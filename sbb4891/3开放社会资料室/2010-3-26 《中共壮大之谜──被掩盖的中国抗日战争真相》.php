																											<div id="postmessage_534609" class="t_msgfont">谢幼田：毛泽东为何在阶级矛盾缓和的江南也强行土改<br />
<br />
作者：谢幼田&nbsp; &nbsp;发布时间：2010-3-21 0:31:48&nbsp; &nbsp;<br />
<br />
1947年9月制定的《中国土地法大纲》规定：“乡村中一切地主的土地及公地，由乡村农会接收，连同乡村中其他一切土地，按乡村全部人口，不分男女老幼，统一平均分配。”“乡村农会接收地主的牲畜、农具、房屋、粮食及其他财产，并徵收富农的上述财产的多余部份。”（中央档案馆编：《解放战争时期土地改革文件选辑》，中共中央党校出版社，1981年，北京）中国的农民，可能拒绝吗？眼见农村贫困和落后的知识分子，似乎对于重新分配土地来解决的办法，从道德上也抱着希望。<br />
<br />
<br />
　　毛泽东的策略制定得很周详，他在1948年2月15日指示：“新区土地改革应分两个阶段。第一阶段，打击地主，中立富农。又要分几个步骤，首先打击大地主，然后打击其他地主。对于恶霸，对于大、中、小地主，在待遇上要有区别。第二阶段，平分土地，包括富农出租和多余的土地在内。但在待遇上，对待富农应同对待地主有所区别。总的打击面，一般不超过户数百分之八，人口百分之十。”（《毛泽东选集》第四卷，合订本第178页，人民出版社，1967年，北京）<br />
<br />
<br />
　　可是，这占人口百分之三、四的地主，是建立在地主占有农村百分之四十到五十土地的假定之上的。而在实际生活中，中国的土地问题，和官方所假定宣传的差异很大，老百姓对地主的仇恨不是轻易就煽动得起来，而且地主不是一小撮，而是一大堆。<br />
<br />
<br />
　　清代中期以后，地主的地权在全国范围内普遍减弱，只要交出一定的佃金，就可以租地。往往租进土地者，又租与另外的人耕种。不仅在江南、华南、华中、四川，甚至在遥远的东北都是如此。这样，地主所占有比例当然不止毛泽东所说的百分之十。东北土改中按照拥有土地，并且租佃给他人耕种，以剥削为生的就是地主的原则，打击面竟然达到百分之二十五！这当然会给中共带来政治方面的问题，以至于土地改革运动的具体主持人刘少奇不得不对此加以处理：“2月7日，致电中共中央，汇报东北土地改革中打击面太宽的问题，指出：‘东北土地斗争中打击面占人口百分之二十五，实在太多，其中定有很多佃耕地主土地但又雇人耕种的农家，这种人一方面受剥削，另一方面又剥削别人，其中有一部份剥削与被剥削相抵，所余不多，应定为中农；另一部份，则有相当大的资本，剥削占其收入的主要部份，但这种剥削基本上应认为是资本主义性质的。为了缩小打击面，除区别对待地主的大、中、小及富农的恶霸与非恶霸外，在东北是否应将富农的圈子划小一点，并将富农亦分为大、中、小分别对待。即将剥削与被剥削相抵，剥削部份不得超过总收入百分之二十五或三十者划为中农，除平分土地外，这种中农的财产不动，对这以上的中、小富农在徵收其牲畜、农具、房屋时，给一部分代价（价款由地主封建财产中支出，或由政府发一次公债），其粮食及其它财产不动。”（中共中央文献办公室编：《刘少奇年谱》下卷第126-127页，中央文献出版社，1996，北京）参加所谓剥削的人实在太多，与意识形态的规定不符合，刘少奇只好把其中的一部份划入中农。马列主义的封建主义论出自欧洲中世纪的社会，本来就不符合中国的国情，领导土改运动的刘少奇出于策略修改了原则，他的电报证明了“造反无理”。<br />
<br />
<br />
　　正因为农村的土地占有问题并不尖锐，在江南一带，甚至一再普遍出现江南无土地改革必要的论点，以至于官方通过不同管道加以驳斥。上海《大公报》1950年2月8日为此发表社评说：“这些错误论点就是‘江南无封建’。有些人甚至公开宣传、叫喊、或者给政府写信，坚称江南农村是没有封建的。以为土地改革，只应在北方有封建的农村去实行，在江南不行。……他们认为江南无封建的理由就是江南文化进步，交通便利，工商业发达，封建所有制已经不存在了，或者存在很少了。”作为大报纸的社论，当然以毛泽东阶级斗争的理论为指南，却在的官方宣传土改必要的言论中，透露出在江南农村地主农民矛盾并不尖锐冲突的记载。<br />
<br />
<br />
　　在北京中央宣传部主办的理论杂志《学习》的问答栏目中，专门解答了这问题。解答分为三个部份，第一部份回答“江南土地分散，地主很少”，主要公开把公用地算作地主名下，以便符合当时官方公布的统计：“江南土地是很集中的，地主的人口与户口也如全国其他地区一样，占着总户口、人口的一定比重。如华东的苏南、浙江、皖南三区，大约占农村人口百分之三至四的地主户口，即占有农村总耕地的百分之五十到六十的土地（包括公地在内）。在中南区的湘、粤、赣、鄂四省土地虽较华东分散，占人口百分之三至四的地主，也占百分之五十左右的土地（连公地在内）。”注意，毛泽东在他的调查中明明说公用地占百分之十，却在统计中统统算做地主的土地。第二部份是驳斥江南“地主收租租额很轻”的说法：“其轻的原因，并非地主兼营工商业而对土地剥削收入不重视，而是因为近十余年来，敌伪与蒋匪反动政府徵收田赋很重，地主为了逃避这种负担，不得不以‘减轻’一些地租为名，以便要佃农代交负担。”文中的“减轻”二字是打了引号的，不过恰恰由中央宣传部门承认了地主收租轻的事实。至于减轻对农民收租以后怎么会“逃避负担”，让人不解。第三部份的驳斥理由，反而清楚地说明，中国的地主地权减弱的土地制度对农民是有好处的：“至于第三点理由：‘江南佃农多有田面权，这种土地对佃农没有什么封建性的约束。’农民有了田面权，只要农民按时交了租子，农民就可以自由地使用土地。这对农民与生产来说，当然是有好处的。但田面权的取得是过去农民斗争的结果，或是农民用代价换来的，而不是地主对农民的恩典，同时也不因为有了田面权地主就不剥削农民了。”（《学习》总第九期，1950年8月15日）文章并没有回答“没有什么封建约束”，即土改有无必要的问题，而是承认农民“自由使用土地”和“从中得到好处”，至于这些好处是否是斗争得来，是另外一个问题。如今中国大陆的国家银行贷款给私人作生意，天经地义是要收取利息的。私有的土地租借给人要交租，却是罪恶滔天。不收利，不收租，白送吗？只要人类结成社会，就有一定的社会秩序，就有社会伦理和相应的社会组织；只要存在私有制，就有贫富差别，就应当由社会自身或者国家来调节。中共的阶级仇恨理论经得起推敲吗？<br />
<br />
<br />
　　同样，在中宣部所编辑的《学习》杂志总第十七期，关于江南土地制度中的田底权和田面权问题的解答，进一步显示传统的土地制度削弱地主的土地权的许多长处：“土地改革法第十二条关于田面权的规定是在江南新解放区实行土地改革中，解决农村复杂的租佃关系上，一个重要决定。因为在江南农村中田面权（即佃农对土地的永佃权）与田底权（即土地所有权）是分开的，且各有其价格。田底权一般为地主所有，田面权一般为农民所有。租入土地的农民虽然有田面权，但每年仍须向具有田底权的地主交纳田底租（俗称大租），假若租入土地的农民自己没有田面权时，他不但须租得田底，亦必须租得田面，始能进行农业生产。这种佃农除向田底权所有者交纳大租外，还必须向田面权所有者交纳小租。田底、田面（有人称大田小田或大卖小卖）均可买卖、出租和抵押的。抗日战争以前田底价格贵于田面，抗战后逐渐变化，田面贵于田底。至于解放前，田面价格则上升很高，田底价格下降更甚。据常熟县虹桥、葛城、董滨三个乡的调查，抗日战争前每亩田底与田面价格均为五至十石，抗战以后每亩田底三至四石，田面五至八石；解放前每亩田底一石，而田面则达六至十二石。这种变化是与革命的胜利分不开的。这种变化说明着封建统治势力日趋崩溃，人民的势力日益成长。”（《学习》杂志总第十七期，1950年8月16日）这里透露的是，中国农村土地制度通过地权减弱，和平地的演进，永佃权和所有权巧妙地分开，农民和地主的对立当然就缓和。这是从过去和现在各方面都承认的铁的事实。现在拥有田底权者被当做罪恶昭彰，拥有田面权者是革命者。这土地权分开的事实否定毛泽东的《矛盾论》中所谓的地主和农民关系是你死我活的对抗性矛盾，而且否定了这矛盾是必须以一方消灭一方为唯一解决方法。<br />
<br />
<br />
　　从刘少奇的报告中透露在东北拥有土地并且出租谋利的人，占人口25％，加上富农占有土地大约30％，加起来就超过半数人口，再加上中农所占有的土地，比例就更高，而决不是毛泽东在《兴国调查》中的1％的地主占有土地40％，毛的数字是为了他在井冈山制造阶级斗争做根据的，也是后来暴力土地的主要理由，显然是为了政治利益的需要编造的意识形态神话。中国从来是地主、富农、中农多数人拥有土地，而且富者变贫穷，贫穷变富有并没有在制度上有任何歧视性的阻碍，就使得中国农村中农民和地主的矛盾更多是缓和的，甚至在引起暴动的饥饿时期也仍然是农民和政府的矛盾尖锐；而且乡村的伦理本位的社会价值观，和普遍存在的公田、族田，以及县、乡存在的义仓，使得家族内部或者村落里可以互相调节，同舟共济。毛泽东的编造也许依据马列主义，马列主义出于欧洲，欧洲中世纪的农村（其实日尔曼人大迁徙以后的欧洲极其黑暗、野蛮，好几个世纪都没有城市、商业），是以居住在城堡中的具有武装的农奴主和人身依附于他们的农奴组成，阶级分明，对立明显，欧洲至今仍然存留着许多这样的石头城堡。把欧洲的概念硬搬到中国，制造毛泽东思想价值观，以此搞阶级斗争，在阶级斗争中火中取栗，夺取政权。</div>

							
							
							
							
