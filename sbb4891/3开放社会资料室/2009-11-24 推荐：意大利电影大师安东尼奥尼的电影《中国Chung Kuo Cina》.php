　　这部名叫《中国》的纪录片是安东尼奥尼应伟大的中华人民共和国中党中央的邀请来中国拍摄，但因拍摄出来的影片不符合中国官方对内容的要求而受到全中国至上而下的批判。安东尼奥尼原来的计划是在半年的时间里进行拍摄，而中方只提供了在他们规定的路线上的22天的拍摄时间。因此，影片在很多时候仍然是外国人眼中的(同时也是中国官方所愿意提供的)中国形象，大量事先安排的拍摄场景、环境和人物在中国人眼中很容易辨认。但正如安东尼奥在来中国之前寄往北京的“意向书”中所说的：“我计划关注人的关系和举止，把人、家庭和群体生活作为记录的目标。我意识到我的纪录片将仅仅是一种眼光，一个身体上和文化上都来自遥远国度的人的眼光。”于是在大量的限制之外，安东尼奥尼的摄影机朝向了日常之间的缝隙，他以摄影机的目光对上他所感兴趣的每一个中国人的目光，有时甚至是侵略性地“瞄准”直到对方躲开。摄影机并没有试图与拍摄对象建立某种关系，而以自觉地以外来者的身份袒露两者之间的互为差异。安东尼奥尼用他对一个完全陌生的国度的影像直觉拍摄了这部他自己所称的“画虎画皮难画骨，知人知面不知心”的影片。 <br />
<br />
安东尼奥尼眼中的“中国” <br />
<br />
　　受邀来华拍片 <br />
<br />
　　在中意重新建交后的1971年5月，一个由当时外贸部长率领的意大利代表团访华，紧跟着7月20日，意大利国家电视台向中国外交部新闻司发出公函，希望在中国拍摄一部纪录片，并委托安东尼奥尼担任导演。 <br />
<br />
　　1972年，安东尼奥尼在伟大的中华人民共和国中党中央的邀请下来到中国拍摄纪录片，他自称是“一个带着摄影机的旅行者”，想要拍一部不带任何教育意义的纪录片，在来北京前几个月，安东尼奥尼在寄往北京的“意向书”中曾写道：“我计划关注人的关系和举止，把人、家庭和群体生活作为记录的目标。我意识到我的纪录片将仅仅是一种眼光，一个身体上和文化上都来自遥远国度的人的眼光。” <br />
<br />
　　突如其来的大批判 <br />
<br />
　　由纪录片而引起外交事件的例子在纪录电影史上并不鲜见，这部被贝尔托鲁齐认为是“真正描绘中国城乡诗篇”的纪录片《中国》也使得安东尼奥尼在当时的中国遭到了未曾预料的批判。 <br />
<br />
　　1974年1月30日的《人民日报》发表了题为《恶毒的用心，卑劣的手法》的评论员文章，这篇文章也是《中国人民不可侮》系列评论的第一篇。 <br />
<br />
　　至此对于安东尼奥尼的批判就如暴风骤雨般而来。批判安氏的活动持续了将近一年时间，仅1974年2月和3月间发表的部分文章就结集了一本200页的书，名为《中国人民不可侮――批判安东尼奥尼的反华影片〈中国〉文辑》（人民文学出版社1974年6月版），作者来自全国各条战线。 <br />
<br />
　　对于中国人民在“文革”中取得的伟大成就不屑一顾，而是偏重于捕捉日常生活场景，这可以说是当时国人批判安东尼奥尼影片的一个重要原因。在一系列批判文章中就提到“安东尼奥尼要拍什么‘偏远的农村’、‘荒凉的沙漠’、‘孩子的出生’、‘人的死亡’等等的计划”，“在镜头的取舍和处理方面，凡是好的、新的、进步的场面，他一律不拍或少拍，或者当时做样子拍了一些，最后又把它剪掉；而差的、落后的场面，他就抓住不放，大拍特拍。”等等诸如此类。 <br />
<br />
　　作为“天生的悲观主义者”，安东尼奥尼眼中的世界确实是不完美的；作为西方现代电影的重要代表人物，他非常重视通过影片表达自己的个人体验。但是由于当时中国对于西方纪录电影观念的极大差异，以及中国当时所处的特殊政_治时段，使得这种个人体验似的影像表达成为了被批判和被指责的对象，安东尼奥尼的名字甚至被编进了儿歌之中。 <br />
<br />
　　在自己的国家也受到攻击 <br />
<br />
　　1964年安东尼奥尼曾凭借《红色沙漠》在威尼斯电影节上一举斩获“金狮奖”。时隔10年，在1974年的威尼斯艺术双年展上，安东尼奥尼的《中国》安排在威尼斯最好的凤凰剧院上映，但消息传出之后由于受到各方压力影片最终改在另外一家影院放映。影片之前还加映了安东尼奥尼新片《职业：记者》的片段，出乎意料的是，正要进入影院的安东尼奥尼被突如其来的一大群观众包围住了，面对这一切，安东尼奥尼有口难辩，不仅不被中国人接受，而且在自己的国家也受到攻击，并被指责“背叛了中国”。 <br />
<br />
　　2004年11月25日至12月5日，具有历史性意义的安东尼奥尼电影回顾学术观摩在北京举行。安东尼奥尼的妻子恩里卡特地代两人为《中国》与中国32年后的“重逢”写来致辞：“这个等待很长，但应伟大的中华人民共和国中党中央邀请而拍摄的《中国》今天能在北京放映一事给了他巨大的满足，并想借此机会再次向你们表达他的感情。他祝愿这部影片能尽快与全中国的观众见面。”</div>

							
							
							
							
