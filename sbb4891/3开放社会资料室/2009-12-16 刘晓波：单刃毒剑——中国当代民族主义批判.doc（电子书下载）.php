序言：通向极权暴政的现代双轨<br />
<br />
民族国家的兴起是人类现代史的一部分，民族主义也就必然是现代性的重要组成部分。谈起民族主义，通常有所谓的“善恶双刃剑”之说。象世俗化、理性化等现代性具有两面性一样，民族主义也是一柄双刃剑，它可以在各民族对大帝国霸权的挑战中，激励民族国家的独立，导致主权优先的国际关系；它也可以在民族扩张对世界秩序的挑战中，煽动起法西斯主义、军国主义、各类宗教原教旨主义及其恐怖主义的泛滥，导致狭隘的主权主义对普世的人权原则的践踏。正是这种现代性的无限膨胀，使民族主义和种族主义成为极权主义的有效的政治工具。<br />
然而，在我看来，民族主义并非在所有的历史阶段和国际环境下都是“双刃剑”。作为善恶双刃剑的民族主义，主要适用于民族主义发源的殖民时代，殖民地人民对殖民者霸权的反抗，既是争取民族独立的斗争，更是争取人民自由的斗争。而当外部危机基本消失的和平时代来临，特别是在后殖民后冷战的和平环境下，当国际大势的走向以自由民主为主导潮流之时，民族主义的鼓噪往往要走向反面。特别是独裁政权煽动的民族主义，甚至连“双刃剑”都不是，而只是单刃毒剑――恶棍的最后避难所。独裁民族主义是政客们弄权的意识形态工具，也是一个心智不成熟的民族坠入蒙昧深渊的标志。<br />
国家独立和人的解放是近代民族主义的两大支柱，为近现代世界留下了民族主义和自由主义两大政治遗产。或者说，抵抗外族的武力入侵和欺压的斗争，只有当争取民族独立（包括独立、自决、自治等权利）与争取人的自由（包括尊严、平等、民主等权利）相一致时，民族主义才具有道义正当性和积极意义。比如，18世纪美国的独立战争，19世纪殖民地人民争取独立的斗争，20世纪印度等殖民地的先后独立；在当今世界，独裁国家中的少数民族争取民族自决和高度自治的斗争，也带有殖民时代的特征，既是民族主义，也是普世主义，前者指向争取民族自治权利，后者指向争取民族平等和少数族群自由等基本人权（如前苏联各加盟共和国、中国的西藏、南斯拉夫的科族、东帝汶等）。<br />
然而，反殖民的斗争一旦放弃了“人的解放”的自由主义目标，就将陷于“国家至上”的极端民族主义泥潭，反侵略战争的胜利也将随之变成独裁的胜利，接下来的民族主义，很可能是独裁者鼓动起的帝国式狂妄，称霸世界的对外扩张也就不可避免。而为独裁者的帝国野心付出最大代价的只有人民。在二十世纪的两次世界大战中，人民为独裁者的权力野心付出代价之大，几乎就是“人的毁灭”――不仅是生命和财产的代价，更是彻底丧失的尊严和自由的代价。<br />
