																											<div id="postmessage_156438" class="t_msgfont"><a href="http://woeser.middle-way.net/2009/07/blog-posthttp://woeser.middle-way.net/2009/07/blog-post_10.html" target="_blank">http://woeser.middle-way.net/200 ... 7/blog-post_10.html</a><br />
王力雄：中共的椅子杂技<br />
<br />
原来我总是试图说服少数民族朋友，不应该把他们受的苦难看作民族压迫，因为汉族也一样受压迫。中国各民族的苦难根源都在专制制度，改善苦难的处境不是各民族相互斗争，而是团结起来改变社会政治制度。然而，对毛泽东时代也许可以这样说，各民族所受的迫害是同样的，但是在1990年代以后，少数民族所受压迫明显超过了汉族。尤其在新疆，汉人已经与政府结成压迫当地民族的同盟。<br />
<br />
民族问题从政治压迫变成民族压迫，是一种危险的变化。如果是政治压迫，只要改变政治，压迫就可以解除，各民族还是可以在一起共建新社会。而若少数民族认为压迫是来自汉民族，政治的改变就不会根本解决问题，只有民族独立才能解除压迫。这对中国的政治转型会非常不利，因为改变政治制度不仅不会使少数民族留下，反而会借转型期的国家控制力衰弱追求独立。那一方面会缩小中国民主力量的发言权和活动空间，给法西斯主义和大汉族主义提供土壤，另一方面将会带来汉民族与少数民族之间的冲突和仇杀。<br />
<br />
不幸的是，新疆的情况正在向后者发展。中共把民族主义当作蛊惑和煽动的精神工具，的确起到了把新疆汉人拉在中共一边的作用，但同时也把当地民族推到了敌对一边。那种敌对不仅是对中共政权的敌对，还是对整个汉族的敌对。我一直奇怪中共的一厢情愿，居然可以指望一个“中华民族”的人造概念把五十六个民族统一成一个民族，再以民族主义的鼓动使整个中国按照它的指挥棒一致对外。然而只要“中华民族”的概念不被其他民族认同，被鼓动起来的民族主义就不会只充当政权的武器，而是每个民族都可以为我所用，用民族主义煽动和凝聚自己的人民，并作为追求民族分离和独立的根据。<br />
<br />
中共在权力运作方面有极高造诣。几千年的权术文化在中共这里集大成，被发展到炉火纯青、登峰造极的高度。旁观中共的权力运作，我眼前常出现那种椅子杂技的表演场面――椅子一张接一张架起来，上面有人在做倒立、滚翻等技巧，越架越高的椅子摇摇晃晃，全靠上面的人掌握平衡，保持不倒。今日中共也达到了这种令人叹为观止的水平，椅子已经架到了不可思议的高度，仍然还能维持平衡。然而平衡不会无限地维持下去，椅子也不可能无限地架高，总会有一个时刻，所有椅子哗啦一下垮掉，那时架得越高，垮得就会越狠。对它必将垮掉，我毫不怀疑，唯一不能确定的只是时间而已。<br />
<br />
与其权术造诣相反，中共权力集团却极少人文精神。中共执政的半个世纪，人文传承被割断，人文教育被置于无足轻重的边缘，即使是今天受过良好教育的新生代官僚，也多是单一化的技术型人才，有知识而无心灵，崇拜强大蔑视弱小。他们依仗的只有权力体系和权谋手段，擅长的唯有行政与镇压，动辄挂在嘴边的加大力度、严打、重典等，一时似乎有效，却是治标不治本，甚至是饮鸩止渴。人文精神的缺失使权力集团无法面对文化、历史、信仰、哲学等更为深入的领域，解决问题的方法诡诈却单薄，只能以应急救火的方式平息事件。而民族问题恰恰首先是人文问题，必须具有人文的灵魂才能找到正确之道。从这个角度看，中国的民族问题今日走入死胡同有一种必然。而展望未来，也难以指望中共能够突破，因为人文的复兴绝非可以召之即来。</div>

							
							
							
							
