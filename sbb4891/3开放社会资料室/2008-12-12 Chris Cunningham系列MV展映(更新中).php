													<h2>Chris Cunningham系列MV展映(更新中)</h2>
						
						
																											<div id="postmessage_9642" class="t_msgfont">关于其人：<br />
<img src="http://img1.mtime.com/mg/2008/12/0b1e8278-b3fe-44c7-8907-ff1b6c2f7931.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
我对Chris Cunningham这个英国人的感觉是“又爱又怕”。“爱”他是因为他的与众不同和才华横溢。最为世界顶级MV和广告导演，看过他的作品的人都不会怀疑他的才华。他也是我最赞赏的全球三大MV导演之一。很多人，即便在美国和欧洲，可能听到他的名字时候都头脑一片空白，但是大家基本上都看过他的作品，特别是那些电子乐和摇滚乐的爱好者。即使像比约克和麦当娜这样对MV导演非常挑剔的主流音乐人都指定要和他合作，可见Chris本人的魅力。而说到我“怕”他， 那是因为基本上他的所有作品都非常“黑暗”。每次翻出他的作品集看的时候，有个别的MV根本不敢看，如果不是跳过去，就是遮住眼睛听听音乐了事。<br />
<br />
Chris Cunningham在过去的4年内基本没有任何作品。本来花了3年时间在两部电影上，但是因为从MV转拍电影长片技巧上的要求有很大的不同，所以任性随意又很完美主意的他最后还是完全放弃了。剩下的1年时间花在制作Rubber Johnny（“橡胶人Johnny”)上。Rubber Johnny讲的是一个被关在地下室的长相恐怖的异形的故事。Cunningham始终还是脱离不了黑暗的诱惑。<br />
<br />
在Chris Cunningham的所有作品中，我认为最优秀的分别是：1997年给Portishead拍的 “Only You”， 1998年给麦当娜的“Frozen”, 1998 年给Aphex Twin的Windowlicker, 1998年接拍的比约克的“All is Full of Love”和2000年的Flex. 前面4个都是MV, 而Flex是实验短片。<br />
<br />
1997年和1998年是Chris Cunningham的丰收年。这一年中他给Aphex Twin拍摄的“Come to daddy”和Portishead拍的 “Only You”都获得了很高的评价。特别是“Only You”可以说不仅在当时，即便现在看来也是非常前卫的。Chris说这个MV的灵感来源与他小时候经常做的一个恶梦。他经常会梦见自己在被埋在水底，呼吸困难，拼命都想往水面上游去。在MV中这个梦境应当是被很好的再现了的。也许这也是他为什么会选择一个小男孩的原因吧。MV的音乐和图像完美结合，节奏非常慢，但是相得益彰，让我觉得似乎音乐是专门给图像设计，而图像是特地给音乐定做的。Chris选择在水中拍摄人物的动作，主要的原因在于水下拍摄会让人的面孔看起来很死寂和冷酷，而且皮肤的质感也不同。（。。）<br />
<br />
正式由于1997年的成功，Chris1998年拍了3个让他名声大振的作品。虽然跟麦当娜合作对他来说是一个新鲜的体验，因为麦当娜的MV一般都有很高的预算，但是Chris最后还是觉得不太适应。因为高预算同时意味着没自由，所以这跟他一贯的作风的确是相反的。<br />
<br />
跟比约克拍摄的机器人，据说后来成为Will Smith的科幻电影“I, Robot”（“我，机器人”？不知道是不是这么翻译的）的灵感和拷贝来源。看过电影的人难道不觉得里面的机器人长得跟MV里面那皮肤像白瓷一样的机器人很相像吗？<br />
<br />
在我选出的4个MV中最搞笑最让人大跌眼睛的是Aphex Twin的Windowlicker。这个MV的前奏，也就是对话那一段非常冗长，但是后来音乐开始以后就非常好看了。其实这样的安排很吊人胃口。因为在听了那么长的废话以后，忽然来了那么“长”的一辆房车，而且出来的人居然是一个长相那么搞笑的但是跳舞又居然何以跟MJ相较量的‘男人’，接下来所有的女人变男人，老女人耍宝的搞笑喜剧就上演了。这个MV是我最喜欢的一个。<br />
<br />
总的来说，Chris Cunningham的作品里面基本上有两个主题：机器人和“解剖学”。而且特别喜欢机械类的东西。解剖学当然不是医学上的含义，而是说跟人的身体结构有关。就像Flex里面的2个身体完全扭曲在一起的人，又或者是Rubber Johnny，其实都是和人的身体的解析有关的。Chris Cunningham沉寂了好几年，他自己说自己是那种“一阵一阵”的人。也就是说，如果不做就很长时间没有作品，如果一开始做就会有很多作品问世。但愿不久又能看见他的作品，但是真的很想拜托他别在弄橡胶人那么恐怖的异形了。<br />
