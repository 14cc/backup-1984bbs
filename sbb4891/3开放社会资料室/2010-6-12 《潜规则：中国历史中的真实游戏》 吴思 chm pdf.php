<strong>编辑推荐<br />
</strong>　　《潜规则:中国历史中的真实游戏(修订版)》一书的作者是发现和系统研究中国历史“潜规则”的第一人。用作者的话来说，他当初（十年前）发现“潜规则”概念时，“心中窃喜”！没想到的是，他的这种因长期潜心研究而豁然开朗的重大发现，不仅在作者的内心产生了巨大的波澜，也让中外历史文化研究者及广大读者倍感震惊!从此，“潜规则”一词，便滥觞于各大媒体的显要版面，也让数百万乃至数亿读者耳熟能详。为此，有媒体称作者的惊人发现，成就了中国“21世纪第一词”，作者也荣膺“潜规则概念之父”的桂冠。<br />
什么是潜规则？作者在《潜规则:中国历史中的真实游戏(修订版)》一书中有详尽的阐述。简单地说，就是在我们的现实“规则”之后所隐藏着的另一种规则，这种规则才是人们（尤其是官僚权势集团）真正遵循的行为准则，这种行为准则与他们的利害计较紧密相联。作者是这样论述的：“在仔细揣摩了一些历史人物和事件之后，我发现支配这个集团行为的东西，经常与他们宣称遵循的那些原则相去甚远。例如仁义道德，忠君爱民，清正廉明等等。真正支配这个集团行为的东西，在更大的程度上是非常现实的利害计算。这种利害计算的结果和趋利避害的抉择，这种结果和抉择的反复出现和长期稳定性，分明构成了一套潜在的规矩，形成了许多本集团内部和各集团之间在打交道的时候长期遵循的潜规则。这是一些未必成文却很有约束力的规矩。我找不到合适的名词，姑且称之为潜规则。”<br />
　　《潜规则:中国历史中的真实游戏(修订版)》内容大体都在讲“淘汰清官”，解释清官为何难以像公开宣称的那样得志得势，为何经常遭遇被淘汰的命运，以至青天大老爷竟成为我们民族梦的一部分。中国历史上之所以少清官，甚至淘汰清官，选择恶棍，正是官场“潜规则”发挥效应的结果。《潜规则:中国历史中的真实游戏(修订版)》好看的地方就在于，它不仅让你拨开层层迷雾看清历史的本质与真相，让读者花最少的时间和精力洞穿历史的内核，还将尘封的历史复活，在历史人物命运及日常生活细节中，感受一种独有的来自历史本身的巨大的、悲天悯人的力量――这也正是历史本来应该具有的、但一直以来很少能有的力量。<br />
　　比如，作者在揭示官场中“淘汰清官”的潜规则时，作者写道：海瑞是一个肯定不贪污不受贿，也不接受任何“灰色收入”的清官。这位清官在浙江淳安当知县的时候，穷得要靠自己种菜自给，当然更舍不得吃肉。有一次海瑞的母亲过生日，海瑞买了二斤肉，这条消息居然传到了总督胡宗宪耳朵里。第二天，总督发布新闻说：“昨天听说海县长给老母过生日，买了两斤肉！” <br />
　　作者在论述“新官堕落定律”时，也这样史论结合：朱元璋是明朝的开国皇帝，他讨过饭，打过仗，从一个马弁干起，最后得了天下，对人情世故的了解相当透彻。他当然明白自己给官员定的工资不高，所以，在地方官上任之前，他经常要找他们谈一次话，讲讲如何正确对待低工资，如何抵抗贪污受贿的诱惑。他会给自己的部下算一笔很实在的利害关系帐。<br />
　　朱元璋说，老老实实地守着自己的薪俸过日子，就好像守着井底之泉。井虽然不满，却可以每天汲水，泉不会干。受贿来的外财真有益处么？你搜刮民财，闹得民怨沸腾，再高明的密谋也隐瞒不住。一旦事发，首先关在监狱里受刑，判决之后再送到劳改工场服苦役，这时候你那些赃款在什么地方？在数千里之外呢。你的妻子儿女可能收存了，也可能根本就没有。那些赃物多数藏在外人手里。这时候你想用钱，能到手吗？你家破人亡了，赃物也成了别人的东西。所以说，不干净的钱毫无益处。<br />
　　这样的利害分析也算得透彻了，但实际上并没有起到多大作用。派下去的官员，如同冒着枪林弹雨冲锋的战士，一排排地被糖衣炮弹击中倒下。前赴后继，一浪接着一浪，一代跟着一代。后来，朱元璋当皇帝当到第十八个年头（公元1386年），这种现象见得多了，便总结出了一条规律。朱元璋说，我效法古人任命官员，将他们派往全国各地。没想到刚刚提拔任用的时候，这些人既忠诚又坚持原则，可是让他当官当久了，全都又奸又贪。我严格执法，决不轻饶，结果，能善始善终干到底的人很少，身死家破的很多。<br />
　　请留意中间那一句话：“没想到刚刚提拔任用的时候，这些人既忠诚又坚持原则，可是让他当官当久了，全都又奸又贪。”这就是新官堕落定律。 <br />
