<strong>媒体推荐</strong><br />
<strong>　　</strong>“潜规则”<br />
――21世纪第一词。<br />
<br />
　　作者在历史上写下现实的眉批。<br />
――《新周刊》杂志<br />
<br />
　　上世纪90年代，我与吴思形成了一个固定的交往模式，每次从天则所双周讨论会出来，站在方庄的车站上，讨论一个小时问题。没讨论完，经常车来了也不上。通常是他提出观点，然后用故事论证，让我反驳他。就这样，我跟吴思抬杠抬了好几年。《潜规则》中的那些道理和古代官场上的例子，吴思差不多给我从酷暑讲到寒冬，从寒冬讲到酷暑。我当时并没有想到吴思会把这些话题系统化，形成一本书，而且是名著。印象最深的是，只要我往抽象里提炼问题和概念，他就打断，一定坚持还原成经验可理解的东西；坚持从常识中推导出公理，来推演那些元问题，也就是根子上的问题。这样讨论出东西，好处是推理实在，老百姓也能明白；但特别花时间，一个小时不够讨论一个细节的。多年后，我对吴思说，你这种方法很像胡塞尔和孔子的现象学方法，西方叫本质直观，东方叫实事求是（或格物致知）。<br />
――姜奇平，《互联网周刊》名誉主编<br />
<br />
　　我和吴思是在《农民日报》、《桥》杂志时的同事，80年代的时候他总是喜欢背着一个很搞笑的军挎包，到处去寻找事实的真相。1988年我们去过一次大寨，去看一个集体主义的典型在分散模式的时代是怎么解决的。他跟一般记者不一样，除了事件本身的真相之外，还探究社会结构的真相。我们直奔大寨，跟农民在田埂上聊。县委书记和宣传部长听说有找农民的记者，就开车过来了，搬椅子我们坐。跟农民聊怎么能坐着呢，就都站着聊。<br />
　　此人有国士风度，我当时私下认为是同事中能成大器的几个人之一。我对他的评价是“都市里的文化隐者”。他外表看着很漠然，实际上心很热。吴思有很多朋友，但某种程度来讲是一个离群索居的人，他是很孤独的，他不是要解决个人的问题，而是要解决社会的问题。<br />
――刘江波，触动传媒执行副总裁<br />
<br />
　　他和李亚平是同学，都是中国人民大学中文系毕业的，都喜欢到历史中发掘教训。他发明的“潜规则”一词非常流行，一次有人向法国学者马克介绍说，这就是“潜规则”一词的发明者，马克问，“潜规则”是什么意思？吴思用英文回答，说是“隐蔽的规则”。这个词比较贴近生活的历史，让人浮想联翩，比如老百姓说的“猫腻”，古人说的“形格势禁”，还有斯密所谓“看不见的手”，即市场经济的“钱规则”，确实有很多隐蔽的规则。中国，兵法最发达，“兵不厌诈”的意思是：没有规则，就是唯一的规则。这种东西，确实值得研究。<br />
――李零，北京大学中文系教授<br />
<br />
　　我们做研究的人，对潜规则其中的意思都是知道的，这本来是一个学理的概念，也有相关的描述，只是我们都没有人用潜规则来概括。吴思把这个概念形象化了，潜规则一出来，大家一下子就明白了，特别生动。他通过历史来分析，又呼应现代的问题，这样更容易把潜规则变成一个公众概念。吴思开出一条研究历史的新路子，不是很学理，但很有智慧，很有冲击力。这正是过去的历史研究缺乏的。<br />
　　易中天也讲到一些权谋，但易中天的太简单化了。他们都说潜规则，但不同的是吴思对潜规则的态度是批判的，而易中天却在暗中有一种赞美之意，两人的取向不一样。当史学开始大众化以后，我们要考虑如何把握舆论的导向。我们应该让人民向善，而不是向恶。<br />
――张鸣，中国人民大学政治学系教授<br />
<br />
　　吴思是个处变不惊的人，他不追社会热点，不像别的经济学家、社会学家对热点发言，他不做社会的跟班、不做市场的跟班，这一点很可贵。他始终保持着冷静和理性的态度，对社会有通透的认识，做什么事情都会找到自己的一套认知的方法，不受别人的影响。他也是个非常节俭的人，走哪儿都骑自行车，住的房子也非常破，不是他没有钱，而是他应对世界的逻辑，不赶时尚。有的教授、学者聚一起都是谈房子、车子，他不求这个。有一阵他严格按照养生食谱来要求自己，吃几个蛋白、多少卡路里、多少单位脂肪，他都能数量化。在中国繁荣论和中国崛起论叫嚣得让中国人自己大都相信的时候，在中国人多以为“这里就是现代性，就在这里跳舞享受”的时候，吴思从历史老人那里借来了“奥卡姆剃刀”，他那“片面的深刻”有力地拷问着每一个有责任感的中国人。<br />
――余世存，学者<br />
<br />
　　吴思给我最大的启发，是他的概念萃取和创造的办法。大家知道，“潜规则”、“血酬定律”、“官家主义”这些概念，是吴思的萃取和创造，现在已成为流行语汇。可以说吴思丰富了中国语汇，深化了中国思想。有时我想，概念的萃取和创造，也许是衡量一位思想家水平高低的重要标准。没有对现实问题的深刻洞察，就萃取不出来有用的概念。没有学术思考的灵气，就创造不出来可以流行的概念。<br />
　　治史有两种态度，一种是学究性的，去整理和还原历史的材料，另一种态度，是基于对现实问题的关注去研究历史，目的是解决今天的问题。吴思是有深切的现实关注的，他研究的目的，是为了理解中国现实的运行逻辑，是为了寻求解决中国问题的办法。在这个意义上，吴思不是中国历史学家，而是中国历史思想家，或者更确切地说，他是一位中国历史的政治思想家。<br />
――杨鹏，北京天下谷企业家文化研究所所长<br />
