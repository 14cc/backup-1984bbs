</strong>修订本出版说明<br />
　　本书的第一版，由云南人民出版社于2001年1月出版发行。这次修订撤掉了杂编中的两篇文章：《造化的报应》和《探究雷锋》，增加了三篇文章：《潜规则的定义》、《正义的边界总要老》，以及一篇关于潜规则概念的访谈。<br />
　　第一版的开篇处，类似影壁的地方，有一段关于“潜规则”的说明，这次不再收入，节录如下：<br />
　　“在中国历史上的帝国时代，官吏集团极为引人注目。这个社会集团垄断了暴力，掌握着法律，控制了巨额的人力物力，它的所作所为在很大程度上决定着社会的命运。<br />
　　“对于这个擅长舞文弄墨的集团，要撇开它的自我吹嘘和堂皇表白，才能发现其本来面目。在仔细揣摩了一些历史人物和事件之后，我发现支配这个集团行为的东西，经常与他们宣称遵循的那些原则相去甚远。例如仁义道德，忠君爱民，清正廉明等等。真正支配这个集团行为的东西，在更大的程度上是非常现实的利害计算。这种利害计算的结果和趋利避害的抉择，这种结果和抉择的反复出现和长期稳定性，分明构成了一套潜在的规矩，形成了许多本集团内部和各集团之间在打交道的时候长期遵循的潜规则。这是一些未必成文却很有约束力的规矩。我找不到合适的名词，姑且称之为潜规则。”<br />
　　第一版出版后，我对潜规则的认识又有深化，补写了一篇《潜规则的定义》，收在《血酬定律》里，这次移了过来。<br />
　　《正义的边界总要老》为潜规则勾勒了一幅正式法规变迁的背景，又未曾收入《血酬定律》，也收入本书。<br />
　　在“潜规则”概念问世十周年之际，《新周刊》的主编采访了我，追问这个概念诞生的经过和随后的进展。本书编辑提议将访谈录附于书后。我愿意以此作为向读者的汇报。<br />
