<strong>作者简介<br />
</strong>　　吴思，1957年生于北京。1978～1982年在中国人民大学中文系读书。毕业后分配到《农民日报》，先后任总编室副主任、群工部副主任、机动记者组记者。1993年，出任全国新闻工作者协会主办的《桥》杂志社副社长兼中文版主编。1997年至今，供职于《炎黄春秋》杂志，先后任执行主编、副社长、总编辑。著述在国内外引起广泛影响。曾获评为《新周刊》2004新锐榜“年度知道分子”。 <br />
