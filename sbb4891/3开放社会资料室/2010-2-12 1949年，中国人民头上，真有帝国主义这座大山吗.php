													<h2>1949年，中国人民头上，真有帝国主义这座大山吗</h2>
						
						
																											<div id="postmessage_477739" class="t_msgfont">1949年，中国人民头上，真有帝国主义这座大山吗？<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; 王书瑶<br />
<br />
我将就这个题目写三篇文章，分别是针对，在1949年中共取得政权的时候，中国人民头上，是不是真有帝国主义、封建主义和官僚资本主义三座大山。<br />
这是其中的第一篇。<br />
<br />
说在1949年之前，中国人民头上压着帝国主义这座大山，并不是严格的政治用语，更不是严格的学术用语，它只是一种形象的文学笔法，严格的政治用语应该是：当时中国是不是外国的殖民地或半殖民地。不必用多么艰深的历史考证，也不用多么丰富的历史知识，只要看上一两本近代历史的书籍，就可以知道，在1943年一月，当时的中华民国的国民政府，同美国和英国政府签订的新的条约，就可以知道，从那时起，中国就不再是帝国主义的半殖民地了。<br />
<br />
所谓殖民地，就是完全丧失主权的国家；所谓半殖民地，就是部分丧失主权的国家。中国沦为各个帝国主义国家的殖民地，是从1840年第一次鸦片战争之后，通过一系列不平等条约形成的，这些条约，分别是184３年中英《五口通商章程》、1844年中美《望厦条约》、中法《黄埔条约》、1851年沙俄强迫清政府签订的《伊犁塔尔巴哈台通商章程》、1900年沙俄炮制的《俄国政府监理满洲之原则》等等不平等条约。<br />
在所有这些条约中，中国丧失的主权最主要有以下三项：1、领事裁判权，即治外法权；2、割让租借地；3、海关自主权。<br />
在刚好100年中间，中国与各个帝国主义国家签订了太多的不平等条约，割让了太多的租借地，这是一部充满屈辱的历史，外国人可以在中国的任何地方横冲直撞，不受中国法律的约束；中国割让租借了大片的土地给外国人，其中包括将胶州湾租借给德国(租期99年)，旅顺和大连租借给俄国（租期25年），威海卫（租期25年）及九龙（租期99年）租借给英国，广州湾（今湛江市）租借给法国（租期99年）等等。除此之外，在许多中心城市中，还有小片的租借地，比如在上海、天津、武汉等地，都有法租借、英租借、公共租借。这些小片的租借，对中国的文化与政治造成直接的影响，对中国人民的伤害更为直接和严重。<br />
与此同时，由于满清和国民政府向外借债，多以海关关税为担保，中国的海关也为外国人所把持。<br />
<br />
从1843年到1943年的一百年中间，世界发生了很大的变化，一个是各个殖民地和半殖民地国家的人民日益觉醒，争取民族独立的运动此起彼伏，形成强大的反帝运动，迫使帝国主义不得不陆续放弃其殖民统治；一个是帝国主义国家人民的觉醒，他们开始支持殖民地人民的解放斗争；另一个是帝国主义国家之间的战争，也迫使他们不得不放弃对殖民地的统治。后者，对中国政府收回失去的主权，有特别重大的意义。<br />
德、意、日发动的第二次世界大战，首先是严重打击了帝国主义对世界殖民地的划分格局，他们之间的互相厮杀，使他们之间的矛盾激化，削弱了他们的统治力量，他们又为了争取殖民地人民对自己的支持，不得不放弃他们在殖民地取得的许多特权。<br />
1937年7月7日，日本侵略者悍然发动了侵华战争，在不长的时间内，占领了中国大片领土，接着又侵略东南亚，又发动了太平洋战争，其势汹汹；与此同时，德、意两国，几乎在一夜之间，席卷了整个欧洲，法国全国沦陷，同盟国（包括美、英、法与中国等国）处于不利的防守地位，这时中国政府是不是能够坚持抗战，不仅对中国，而且对整个世界大战的胜负格局都有极为重要的影响。<br />
1937年末，汪精卫投降日本，1940年3月，汪精卫在日本人支持下的伪国民政府在南京正式成立，这对整个中国的抗战阵线有很大影响，如果设在重庆的、蒋介石领导的中国国民政府也投降日本的话，日本人必定会有更多的兵力投入东南亚战场和太平洋战场，对整个世界大战的形势必将有利于德、意、日轴心国，而美、英、法的同盟国会处于更加不利的地位，因而，中国政府――这里指的中国政府自然是由蒋介石国民党领导的中华民国的国民政府――是不是能够坚持抗战，几乎成了二次世界大战的关键。<br />
蒋介石领导的国民政府坚持了抗战，抗击了百万日军，对整个第二次世界大战作出了杰出的贡献，也由此，中国人受到了世界各国人民的尊重。这种尊重，是全体中国人民用鲜血和重大的牺牲争得的，我们没有理由贬损他，说中国人民――主要是国民政府领导下的中国人民，――消极抗战是不负责任的。<br />
在中国政府与美、英、法各国共同抗击德、意、日法西斯侵略的时候，在同这些国家结为同盟国的时候，美英法还要保持其对华的宗主国地位，保持其殖民地特权，显然是不能被接受的，显然是于理背谬的，于是，就有了1943年中美、中英新条约的签订。<br />
<br />
中美、中英间的新条约，主要废除了以下强加于中国之不平等条约：1治外法权；2使馆界及驻兵区；3租界；4特别法庭；5外籍引水人等特权；6军舰行驶之特权；7英籍海关总税务司之特权；8沿海贸易与内河航行权；9影响中国主权之其他问题。这样，作为中国是帝国主义国家半殖民地的主要主权，已经基本收回，作为标志，中国已经不再是殖民地国家了。（以上引自《中华民国外交史》，石源华著，上海人民出版社1994年12月第一版。）<br />
<br />
不过，令国民政府领导人蒋介石不爽的是，这个新条约签署于1943年1月11日，而在此之前两天，也就是1943年1月9日，汪精卫伪政权与日本签订了《关于交还租界及撤废治外法权等之协定》，先两日宣布收回租借；其后，意大利与被德国占领的法国政府也相继宣布废除治外法权与“交换”租借。（参见《中国外交史》，吴东之著，河南人民出版社1990年版。）这就使得中美、中英新条约不免有所逊色。<br />
<br />
无论如何，上述与美英各国的新条约，对中国人来说，总是好事情，“消息公布之后，举国欢腾，国民政府颁令，自2月5日到7日放假3天，‘悬旗志庆’。中共中央也决定‘各地在战地环境许可下，均应于旧历元旦前后召开军民庆祝大会”（引自《中华民国外交史》）。中共中央是认同这两个条约的积极意义的。再说中国是半殖民地国家，就没有了根据。<br />
<br />
那种认为，虽然不平等条约废除了，却不等于我们不再是半殖民地的说法是极不负责任的和不符合实际情况的：在殖民统治期间，一方面由于这些国家积贫积弱；一方面，他与其宗主国已经有了极为密切的政治、经济与文化联系，在主权收回之后，在许多方面，仍然在受原宗主国的制约影响，这是正常是逐渐摆脱的过程，不能说，主权收回了之后，仍然还是殖民地或半殖民地。<br />
如果说，在废除不平等条约之后，中国仍然是半殖民地的说法成立，那么，在签订了不等条约的时候，中国并未成为半殖民地的说法就也成立。<br />
<br />
