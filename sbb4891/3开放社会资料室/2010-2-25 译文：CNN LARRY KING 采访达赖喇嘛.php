													<h2>译文：CNN LARRY KING 采访达赖喇嘛</h2>
						
						
																											<div id="postmessage_492744" class="t_msgfont">原声视频：CNN LARRY KING LIVE Interview with the Dalai Lama 收看需翻墙<br />
译文：CNN LARRY KING 采访达赖喇嘛<br />
<br />
感谢推友提供原文<br />
播出时间：2010年2月22日 21:00&nbsp;&nbsp;美东时间<br />
by 译者：译者成员合作完成<br />
<br />
KING:上周达赖喇嘛会见了欧巴马总统。那么你们的会面如何？<br />
<br />
被驱逐的西藏精神领袖达赖喇嘛：很好。当然，他还是位新任参议员，在外交关系委员会工作时……<br />
<br />
KING:好的。<br />
<br />
达赖喇嘛：我曾经见过他一次，他是一位令人印象深刻的年轻政治家。后来在竞选总统时，他打电话向我询问西藏的事。他刚一成为总统后，就告诉我将会与我取得联络，他是个富有同情心的人。去年，在那一天（自言自语）有机会见面（自言自语）。但后来，就在他访问北京前的时候，我一直感觉与他会面不太合适，因为如果……<br />
<br />
KING:你这么说的？<br />
<br />
达赖喇嘛：是的，他也这么说（自言自语）。我完全同意，更好――你知道，因为除了其他问题（西藏问题）外，他也必须非常非常严肃地考虑与中国领导人的会谈。他也非常想谈。所以我们没在那时会面，情况也许更好。<br />
<br />
因此推迟（自言自语）到这个时候。尽管有些困难，你明白，我们还是见了面并且相当愉快。<br />
<br />
KING:宗座[译注：Your Holiness与教宗讲话时的尊称]，你期待着欧巴马总统可以给予什么支持呢？你想从美国得到什么呢？<br />
<br />
达赖喇嘛：实际上，我提到了我的三个坚持。第一个是，推进人类普世价值，创造一个更加美好的世界，一个更加富有同情心的世界，一个和平的世界。相对于此，技术―经济不是和平的最终源泉。真正的和平之源是内心的和平。内在的和平来自大悲悯之心。所以无论走到何处，这都是我（自言自语）一直要提到的首要坚持。这次，我也谈了这些内容。<br />
<br />
在那个层面上，我们都是同样的人类（自言自语）同样（自言自语），和全世界六十亿人一样。所以这是我们的共同利益和共同责任。我提到了这个。<br />
<br />
第二个坚持是，促进宗教和谐。我提到，在这些（自言自语）或者其他方面，欧巴马总统非常支持我。我希望他也能够在这些方面做出富有成效的努力。<br />
<br />
然后就是西藏问题了。你知道，自从2001年我们就已进行了政治领导选举。所以在西藏问题上， 政治方面主要是民选的领导人来负责。我有一些关于这方面的见解，而且当然了，完全--完全同意。<br />
<br />
所以我首先向他提到了我们和中国政府的最新接触情况。除了那些，还有自由国家的难民问题，我们为保护西藏文化和西藏佛教传统等等做出了各种工作。<br />
<br />
我也提到了西藏儿童需要接受现代教育，不仅在西藏外，而且在西藏内的地区。假如一些人（自言自语）有了学历，藏族年轻人将大大受益。<br />
<br />
KING:什么。。。<br />
<br />
达赖喇嘛：我们讨论了这些问题。<br />
<br />
KING:他认同这些吗？<br />
<br />
达赖喇嘛：哦，是的，非常非常认同。实际上，我们会面之后，总统发表了一个声明，美国政府完全支持我的“中间道路”立场。[译注：指达赖喇嘛所称的“不谋求西藏独立，要求实现西藏的高度自治”的道路。]<br />
<br />
KING:现在，此时，你们和中国政府进展到哪一步了？<br />
<br />
达赖喇嘛：现在……（笑声）<br />
<br />
达赖喇嘛：你知道，中国政府目前否认我们之间有问题。或者说，西藏是一个问题。（笑声）<br />
<br />
达赖喇嘛：但。。。<br />
<br />
KING:他们不承认？<br />
<br />
达赖喇嘛：对，他们说藏族人民生活幸福经济繁荣，比以前好得多了。但我们得到的消息却是，某些物质方面是有了发展，但文化方面或宗教信仰方面或所有[精神]领域，藏族人受到了许多压迫、控制、限制。<br />
<br />
例如，几年前，我碰到了一位来自西藏的藏族专业人士。他告诉我他自己（自言自语）的工资，住房和子女的教育，什么都很好。但他说，作为一名藏人，心灵上的，感情上的，某种无法抗的--应该叫什么呢？<br />
<br />
KING:压力？<br />
<br />
达赖喇嘛：可以叫做压力。当他提到这些时，流下了眼泪。那些汉人，一些持强硬立场的汉人是无法理解的。<br />
<br />
（结束录影）<br />
<br />
KING:关于中国和西藏自治，达赖喇嘛的独家采访之后回来。<br />
<br />
（广告时间）<br />
<br />
KING:大西藏自治区的想法可能实现吗？<br />
<br />
达赖喇嘛承认中国政府对他们态度强硬。<br />
<br />
我们陈述了这可能对西藏与中国的关系和进展产生的影响。<br />
<br />
（开始录影）<br />
<br />
KING：当美国人被问到，西藏独立或是美国与中国维持良好关系哪个更加重要时，人们感到相当困惑。但更多美国人认为，与中国保持良好关系比较重要。<br />
<br />
我们能两者兼顾吗？<br />
<br />
达赖喇嘛：事实上，我们并没有寻求西藏独立。没有，你明白，我们提法的是“中间道路”。我们抱怨目前汉人实行的西藏政策，这对藏人的宗教自由、文化继承和，而且也对环境是破坏性的。<br />
<br />
但另一方面，你知道，我们也不想西藏从中国分裂出去，因为西藏是内陆地区，物质上相对落后，每个藏人都想西藏实现现代化。<br />
<br />
因为这个原因，西藏还是中华人民共和国的一部分。从物质发展方面来说，这是符合我们自身利益的。但在文化、教育、宗教这方面，我们需要真正的自治――藏人有更好的方法来处理。 所以在这些方面，藏人应该有完全的自治权利。<br />
<br />
这就是我们所说的“中间道路”。<br />
<br />
首先，我们并不寻求独立；藏人、我们的支持者和朋友中，有些人持一些批评意见，我们并不是为藏独而战。所以……<br />
<br />
KING：但……<br />
<br />
达赖喇嘛：因此，这并不冲突，维持良好关系。<br />
<br />
KING:我明白。<br />
<br />
达赖喇嘛：――和中国维持良好关系。同时，支持，你明白我们的立场。事实上，我们的“中间道路”是维护团结稳定的最好方式。从常识看，我们的“中间道路”是符合中华人民共和国最大利益的。<br />
<br />
KING：但你最近不是说“中间道路”失败了吗？<br />
<br />
达赖喇嘛：是的。2008年3月10日后，我公开表示，我们这方面想要在西藏内部带来进步的努力。<br />
<br />
那些努力失败了，但不意味着我们完全失败了。<br />
<br />
另一方面，我们的方式得到了很多中国知识分子和作家的支持。你也看到了全世界很多政府――我是说，很清楚，包括美国和印度政府，完全支持我们的策略。<br />
<br />
KING：你离开西藏50年了――超过50年了。你想念西藏吗？你常想起西藏吗？你感觉如何？（笑声）<br />
<br />
KING：那是很长的一段时间。<br />
<br />
达赖喇嘛：是的。我偶尔会想起小时候在布达拉宫和罗布林卡的夏宫。有时我会想起这些事情。但这五十年来，我都生活在印度，印度的大米和木豆养育了我。<br />
<br />
（笑声）<br />
<br />
KING:那你不是常常惦记着西藏喽？<br />
<br />
达赖喇嘛：已不是那种形式的思念了。但我们想念着六百万藏族同胞，我们关心他们的基本权利、他们的文化和西藏的环境。这是我们要考虑的主要问题。<br />
<br />
八十年代早期，胡耀邦在任时--他思想很开放，也很务实。那时，他提出了五点建议，有关于我自己。于是我们立即回应了中国政府。我个人的问题不是主要的，重要的是六百万藏人的权利问题，而不是我的权利，这不是达赖喇嘛的个人问题。<br />
<br />
（结束录影）<br />
<br />
KING:接下去的内容：达赖喇嘛的影响力难以置信，他竭尽全力帮助世界上那些处于困境中的少年儿童。<br />
<br />
（广告时间）<br />
<br />
KING：达赖喇嘛说话，人们倾听，他尽已之能唤醒公众并帮助那些无法获得教育、医疗和基本的生活条件的而他们都本应拥有的孩子们。<br />
（开始录影）<br />
<br />
KING:全儿童国际是怎样的一个组织？<br />
<br />
达赖喇嘛：事实上，全儿童国际的组织者去年时来见了我，向我解释他们的工作。我对此印象深刻， 然后我就接受了他们的邀请，来到了这里。<br />
<br />
这非常重要，因为我首要的坚持是建设一个健康和充满同情心的世界，这些小孩是未来的希望。从一开始培养他们的同情心是非常重要的。<br />
<br />
目前，这个组织照顾那些易受伤害的、那些我认为缺少帮助的（自言自语）儿童。<br />
<br />
KING:任何地方？<br />
<br />
达赖喇嘛：任何地方――这是一个国际性的组织。你明白，如果这些儿童缺少关爱， 有一种危险的情况就是，他们很有可能变成无情的人。<br />
<br />
KING:你怎样接触到他们的？<br />
<br />
达赖喇嘛：他们照顾那些无助的儿童，不仅提供食物、住所和教育，而且和他们进行心灵上的交流或（自言自语），所以这是非常非常重要的。<br />
<br />
我认为这是一条真正正确的道路，关爱孩子们的心灵。这非常棒，对此我很赞赏。这就是他们从事的工作。<br />
<br />
KING:你认为他们的工作会起作用吗？<br />
<br />
达赖喇嘛：我不知道具体是哪些人正在从事帮助儿童的工作。但我认为过去几年已经有了些良好的结果，这些结果科学地（自言自语）表明了那些孩子的身体、心灵和智力都有了改善；这些都很棒。<br />
<br />
目前，我唯一的愿望、唯一需要祈祷的就是在全世界推广他们帮助儿童的做法。在昨天上午的一个会议中，我特别提到在有些国家，他们把自己的女儿...你知道，认为她没用。有时会有残害（自言自语）女孩的事情发生。 <br />
<br />
KING:那真糟糕。<br />
<br />
达赖喇嘛：可怕，真的非常可怕。<br />
<br />
KING:哪些人在做那种事？<br />
<br />
达赖喇嘛：哦，起初我从BBC上听到一名中国妇女做了些调查研究，她接受了BBC记者的采访，提到了那类可怕的事情。<br />
<br />
还有印度，有时村民和农民，你知道，他们认为儿子更有用，女儿（自言自语）在印度，也有另一种…… 当然，印度现在是我的家乡，我也一直把自己当作印度的使者，因为我是名佛教徒。<br />
<br />
尤其是，我把自己看作是印度古代nalinda教僧侣制度的追随者。 <br />
<br />
但在印度，你会发现他们的习俗有点像嫁妆或其他。他们认为女儿有时（自言自语）<br />
<br />
KING:你在印度谈论这事吗？<br />
<br />
达赖喇嘛：对，我在印度也说过。<br />
<br />
（结束录影）<br />
<br />
KING:接下去我们会说到海地。<br />
<br />
（广告时间）<br />
<br />
KING：欢迎回来，我们将继续与达赖喇嘛对话。<br />
<br />
我们谈到人类苦难――特别是现在的海地。<br />
<br />
（开始录影）<br />
<br />
KING：作为一个神的子女，一个有原则的人，一个高尚勇敢的精神领袖，你如何对自己解释海地所发生的事情？<br />
<br />
达赖喇嘛：非常悲伤。当然，我常认为自己仅仅是一个佛教徒和尚。我……<br />
<br />
KING：仅仅是一名佛教徒和尚？<br />
<br />
达赖喇嘛：对，我是六十亿人类中的一员――同是人类……<br />
<br />
KING：所以...<br />
<br />
达赖喇嘛：――根本来说我们都是作为人类而存在的。海地的悲剧是非常可怕的。你知道整个国家崩溃了。我昨天刚和一些人谈到我对海地悲剧的感觉。然后我就说了这些。<br />
<br />
如今，不像过去了，重要的是，我认为全世界文明化的启示是，每当面对灾难，例如海啸这些悲剧，其他的人对灾难的反应是重要的。<br />
<br />
同样，类似的，海地这次灾难，你看，全世界都在帮助他们，向他们提供救灾物资。最终，这都要取决于人们自身的决心和信心。<br />
<br />
<br />
我提到，目前重要的是，海地人应该朝前看，不能只是忧虑或是悲伤，要努力工作，恰当利用这些资金，建设新的房屋，新的国家。这就是我的想法。<br />
<br />
KING：所以福从祸中来？<br />
<br />
达赖喇嘛：对，就是这样。<br />
<br />
KING：好的，几天前有人问到泰格・伍兹，高尔夫球手，你说你没听说过他。<br />
<br />
达赖喇嘛：是的。<br />
<br />
KING：他是个佛教徒。他说自己不幸偏离了佛教原则，身陷麻烦。你说过你认为忠诚是种原则。你认为那是种难以实现的信条吗？<br />
<br />
达赖喇嘛：不。原则意味着保护你自己的利益。而另一种维护自己利益的方式是依靠命令，那是不同的。（笑声）<br />
<br />
达赖喇嘛：这些独裁政权发号施令，颁布法律规定。<br />
<br />
KING：这是不同的。<br />
<br />
达赖喇嘛：也是不得人心的。<br />
<br />
但是，你明白，精神上的，道德是要自律。就如医生的建议，照顾好自己的身体。你知道，某些则不应该继续吃，同样精神上也是。<br />
<br />
KING：同样？<br />
<br />
达赖喇嘛：精神上的某些东西应该得到节制，这就是自律。<br />
<br />
KING:你身体如何？<br />
<br />
达赖喇嘛：我很健康。你知道，2008年10月我做了次手术，去除了胆囊。你看，从那时起在我公开演讲时，有几次听众里有些人认为达赖喇嘛能够自愈。但科学上来说，我是不会自愈的。但是，我的身体很好。<br />
<br />
KING：在你经历过这么多后，你还保持乐观吗？<br />
<br />
达赖喇嘛：哦，是的，更加乐观了。<br />
<br />
KING:为什么？<br />
<br />
达赖喇嘛：为什么？<br />
<br />
哦，未来是开放的。毕竟我们在这个星球上生存。我乐观的理由来自对二十世纪的判断，我认为二十世纪后半页的世界比前半页更加健康。<br />
<br />
无论是和平、和解，还是爱与怜悯，甚至是环境问题，世界都在进步。人类更加了解世界的现实情况。 而且我认为在20世纪经历了痛苦，屠杀（有专家分析，二十世纪两亿多人死于战乱）后，人类学会了思考。因此，我很乐观。<br />
<br />
KING:另一件事，你提到了爱，你爱中国人吗？<br />
<br />
达赖喇嘛：当然，我们必须去爱。有时你见到政府强硬的政策，无情的政策，会很愤怒，但那都是一时的。<br />
<br />
KING:你仍旧爱他们（中国人）吗？<br />
<br />
达赖喇嘛：是的，我必须尽力保持爱心。<br />
<br />
KING:谢谢。<br />
<br />
达赖喇嘛：谢谢。谢谢，都十五年了。<br />
<br />
KING:我们上次见面后，都过去十五年了。<br />
<br />
达赖喇嘛：你还是以往那样，祝贺。<br />
<br />
KING：你也还是那样，谢谢。宗座--我没改变？我老多了。宗座，达赖喇嘛。<br />
<br />
（结束录影）</div>

							
							
							
							
