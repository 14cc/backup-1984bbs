													<h2>四月三周两天 :1987，罗马尼亚的色相</h2>
						
						
																											<div id="postmessage_283708" class="t_msgfont">转自 <a href="http://www.douban.com/review/1214007/" target="_blank">http://www.douban.com/review/1214007/</a><br />
<br />
1987年的罗马尼亚。阴冷肮脏的融雪天。铁砣似的交通工具在路上叽叽哑哑。<br />
　　<br />
　　故事发生在短短一日之内。女大学生Ottila在学校宿舍楼里购买走私的进口香烟、食品、香皂 ，为和她在同一宿舍的 Gabita准备着秘密的行李。Gabita 怀孕了，而堕胎在当时的罗马尼亚是非法的行为，她们找到了一个叫Mr.Bebe的男人来私自堕胎。一场噩梦就此开始。在Ottila几经周折找到的廉价小旅馆里，Mr.Bebe提出了要挟，不仅是金钱，也包括Ottila的身体。没有选择的Ottila满足了Mr.Bebe的要求，并且帮Gabita处理了胎儿的尸体：她把他扔进了垃圾箱里。在所有的恐惧与不安结束之后，两个女孩坐在旅馆的餐厅里，沉默地等待她们的晚餐。不远处，是一场婚宴的欢闹场面。而这一天，正是Gabita整整怀孕4月3周2日。<br />
　　<br />
　　描述剧情对某些好电影来说是无关紧要的，因为它听起来乏善可陈。事实上，这是一部非常优秀的电影，如果你能坚持在二个小时忍受住影片表现出的巨大压抑，你一定能够理解为什么39岁的Cristian Mungiu，影片的导演、编剧、制片人能够获得2007嘎那影展的那片金棕榈。<br />
　　<br />
　　外面<br />
　　<br />
　　用导演Cristian Mungiu的话说，这是一部关于友情，责任和爱的电影。故事源自于我们生活中不可对他人言述的经历和隐密。最主要的是，它讲述了一个关于堕胎的故事。1966年，齐奥塞斯库政府颁布了禁止堕胎的法律，直到1989年，齐奥塞斯库死去，社会主义政体在罗马尼亚结束，这项法令才被废除。在此期间，大约有50万的罗马尼亚女性因为秘密堕胎的安全性而屈辱地死去。对于那段历史而言，堕胎，不仅意味着某种错误的、罪恶的道德行为，也关乎于对国家制度的背叛和对抗。<br />
　　<br />
　　影片中军营式的大学生宿舍，冷漠而警惕的旅馆看门人，用西方国家的物品来代替钱的私下交易，乃至堕胎被强加的黑暗色彩，和七、八十年代的中国都是如此相似，而且我相信，类似的故事在中国曾经有更多的版本。因为某种公共制度禁忌而产生的个人经历，以及不可预料的人生选择：为了更好的活下去，或者为了勇气和责任，我们只能象Ottila那样去经历难以启齿的、不堪回首的过程。因为隐密，它甚至不能称之为悲剧，它是划在心里的暗伤，它是被时间吞噬的秘密，多年以后，耻辱感退去，才能够平静地向他人讲述这些经历，如同讲述他人的故事。<br />
　　<br />
　　1987年，东欧政治剧变的前夜。今天的西方评论者和观众在看这种背景的电影时，常常过于敏感地指向政治压迫和隐喻。然而我更相信导演的本意，他在导演阐述里说得非常清楚：“我试着拍摄一部关于人物和故事的电影，而不是关于那个时代，那不是影片的语境和主题。我试着尽可能地尊重并且还原事实，而不是聚焦在对共产主义末期的老一套和标志性上。那个时代在那里，在我的电影里，但是它们在镜头之后：公共汽车噼里啪啦的尾气排放声，形似熨斗般的罗马尼亚轿车，劣质的行李箱，盖满书页的墙，还有当时人们的一些习惯，比如Kent牌香烟可以代替货币在私下流通。没有这些背景，我们将无法理解这部电影。”这段话让我想起参加同届嘎那的娄烨的《颐和园》，在我看来，那是一部首先立意于描述“时代”，而不是人物和故事的电影，既然如此，还是让西方人把政治隐喻和宣传噱头留给《颐和园》这样的电影吧。<br />
　　<br />
　　里面<br />
　　<br />
　　和柔弱而略显自私的Gabita相比，Ottila代表着另一种女性：坚强，勇敢，用中国人的话讲，有情有义，她象Gabita的守护神，承担着种种濒临崩溃的局面，尽管她也无助而惶恐。被Mr.Bebe侵犯之后，Gabita赤裸着下身冲进浴室，用力地搓洗身体，之后她一动不动地坐在浴缸里，她一直背对着镜头，看不见她的表情，也没有任何声音，但是任何人都能感受到这意味着什么样的痛苦。在中途离开旅馆去往男友家的公车上，镜头里远远地，Ottila无声地留下眼泪。她不是不怨恨Gabita，Mr.Bebe的要挟和Gabita之前无意中撒下的谎言不无关系，可是这点怨恨很快转化为对Gabita的牵挂，因为她，是唯一能够帮助Gabita的人，就像她把最后一根烟留给了Gabita一样。<br />
　　<br />
　　影片中有一个堕胎后胎儿尸体长长的特写镜头。4个多月的胎儿，看得十分真切。他被包裹在一块白色的浴巾里，手掌般大小，略具人形，布满血污。 Ottila把他装在书包里，冲出旅馆，镜头一直跟着她，穿过一条又一条黑暗的街巷，我们只能看见Ottila黑暗中晃动的背影，听到她急促而压抑的喘息声，因为害怕和紧张，Ottila迟迟没有找到丢弃胎儿尸体的地方，她的头发在冬天夜晚的雾气里变得潮湿。最后，她跌跌撞撞地走进一个居民楼，迅速地把装着胎儿尸体的书包塞进了楼道里的垃圾箱内。仅有的一点光亮里，她的剪影，静静地站在那个垃圾箱前。<br />
　　<br />
　　就像这场戏一样，影片充满了这种“山穷水复疑无路，柳暗花明又一村”的悬念设置，它们共同营造了一个真实的、极度压抑乃至惊涑的时空，我们仿佛可以清楚地听到Ottila和Gabita的呼吸声和心跳声。Mr.Bebe侵犯Gabita，拍的始终是房间外惶惶然不安等待着的Gabita，没有人知道会发生什么；Gabita独自一人在旅馆里，中途赶去参加男友母亲生日会的Ottila始终心神不宁，打给Gabita的电话几次都被意外打断，打通的电话也无人应答；影片阴沉的调子，Gabita的柔弱形象，似乎象征着一场悲剧的降临，我们和奔向车站的Ottila一样焦急万分，又在最后一分钟为看到还好好活着的Gabita而长呼一口气。<br />
　　<br />
　　令人难忘的还有影片的拍摄方式和演员的表演。Cristian Mungiu决定让影片保持一种阴暗的基调并且避免一切看起来像是经过刻意组织的场面。为了达到真实的效果，他大胆地采用一些不无极端的拍摄方式：长到极致的长镜头--每场戏基本上只有一个镜头；肩扛拍摄，没有斯坦尼康，没有脚架，也没有刻意的晃动；大量的画外音，人物常常在镜头之外说话，观众完全看不见说话者的脸，一切的手段，都为了呈现这部电影最初确立的灵魂：情感和真实。<br />
　　<br />
　　扮演Ottila的Anamaria Marinca令人印象深刻，她之前只是一个在伦敦闯荡的罗马尼亚小演员。导演Cristian Mungiu直到拍摄前一周都没有找到饰演Ottila的演员，他抱着试一试的心情飞去伦敦见Anamaria，他在导演阐述中这样写到： “第一次在夜晚的机场见到她，我很失望，因为她看起来一点也不象Ottila，但是第二天，当我们试戏的时候，改变难以置信地发生了，她完全成为了 Ottila，我看见我的人物通过她的嘴唇被表达出来。她是如此令人难忘，整部电影可以说都是依靠在她的肩膀上完成的。如果说这部电影有什么地方让我觉得完美，那就是演员的表演。”正如Anamaria Marinca的演出，我深信每一个好演员身上都有一种常人未所及的惊人能量，以及或多或少的神经质，当摄影机转动的时候，他们熠熠发光，角色附体，他们是完美的谎言，是世界上的另一个他。<br />
　　<br />
　　好电影都是纯粹而完整的，好电影的好是一个整体。剧本好，导演好，摄影好，演员好，音效好，灯光好，美工好，剪辑好，都好。事实上，好电影让你看不见导演或摄影机的存在，也感觉不到演员个体的存在，所以以上所写只是基于某种背景介绍。其实我们说不出什么，只是更多地了解到一点他人和表象背后的那个世界，或者被感动，或者微小的快乐和悲伤，或者久久无语。<br />
　　<br />
　　电影就是色相，李安说。<br />
