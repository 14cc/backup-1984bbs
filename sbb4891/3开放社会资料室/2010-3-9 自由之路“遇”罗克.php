																											<div id="postmessage_508706" class="t_msgfont">自由之路“遇”罗克<br />
我是一名收集“遇罗克―出身论”系列资料的历史义工，经过多年的努力，收集了大量有关遇罗克先生的“文革”时期资料，并将主要资料翻拍整理上传于网络，共有识之士取用，更希望传承记忆，做遇罗克精神传播者。<br />
<br />
缘起<br />
1992年，外公送给我一面别有毛泽东像章的红袖标，那年在我的人生座标上已经是13岁了。<br />
1993年，毛氏百年冥诞，天朝欲图颂圣以彰威权，不想却触动了我了解文革真相的心弦。开始关注“文革”，幸遇“贵人相助”，收集文革资料，阅读能找到的各种版本的“文革”史料书籍，始知“遇罗克”。<br />
<br />
“遇”罗克<br />
1999年，街边书店偶见《遇罗克：遗作与回忆》，一路小跑回家取钱购之。<br />
2001年初夏，用噙泪的眼睛熬夜看完遇罗文先生写的《我家》。<br />
2001年7月1日，这个令人喜出望外的日子，在潘家园旧货市场意外淘得《中学文革报》1至6期，外加一期专刊。<br />
<br />
追寻“遇罗克”的历史轨迹<br />
结合遇罗文先生的《我家》和《遇罗克：遗作与回忆》提供的线索，萌生了收集整理“遇罗克―出身论”系列资料的想法，于是，每至周末，闲暇时间便成为潘家园之常客。酷夏，骄阳似火，大汗淋漓，汗珠滴落在翻捡的老报纸上；严冬，周天寒彻，十指冻木，仍蹲在地上从上千份老资料中寻觅历史的“密语暗码”。每每找到一份线索中提到过的资料，心中窃喜之极。<br />
“家庭出身问题研究小组”的“论敌辩友”们被我从历史的故纸堆中，从历史的迷雾中一一拎出。起初还设立“辩论台”赞同与驳斥都可刊登，后又不堪压力转向一边倒――担心“辜负了毛主席他老人家的期望”者，如《首都风雷》；从一开始见“出身论”就“怒不可遏”者如《旭日战报》；杀气腾腾，公开宣判“出身论”以死型者如《教工战报》；平等辩论刊载遇罗克文章者如《中学论坛》；发征稿启示，提供批判线索者如《东风报》；图文并茂，用漫画丑化歪曲“出身论”者如《红鹰》；更有狗仗人势公然叫嚣取缔《中学文革报》者如《兵团战报》；如此种种，可以想见遇罗克当年面对的是怎样的“辩友论敌”和复杂情势，真可谓“四两拔千金”。<br />
在收集、整理、阅读、思考这些资料的过程中，我逐渐感觉遇罗克先生已经成为我的老师，遇罗克没有上过大学，可是他的阅历，他的才识配得上中国任何一所名牌大学，当“罗克老师”走入我的心中，进入我的生活时，我已是十三岁的少年，我很幸运，让我在人生与社会“初见”之前就得到了遇罗克精神的洗礼――独立思考。而少年的我，竟然是踏着遇罗克的的血迹步履蹒跚地前行，在曲折的道路上寻觅自由之真谛，思想意识也在不断完善与成熟中。<br />
正是因为有了遇罗克用生命做代价，在反智时代为我们换回“生存的尊严”，我们才能在“正义”的座标轴上继续前行。遇罗克从“奴役之路”而奔向自由，我们不能仅仅成为道路两旁的围观者，更要加入到前行的路，“遇”罗克而同行之。<br />
在与罗克先生一起同行的路上，我们遇到的困难虽然与先生的遭遇不可同日而语，但其抹煞个人价值，掩盖事实真相，强迫人们遗忘历史的病毒基因依然如故。网络屏蔽信息，发贴被“和谐”删除，电子邮件受到监控，打压平和理性的持不同政见。如果说遇罗克先生当年还能钻专制时代的漏洞发声，那么现今想透过网络发声，在发表之前就可以被官方通过软件进行过滤和删除，让真相“胎死腹中”，只能说后极权时代，科技的发展更让独裁者将专制做得更绝，更易于操控，这是对社会进步最彻底的亵渎。这一切说明我们与遇罗克先生所处的时代仍然存在许多类似的困苦与艰辛，仍然要携手并肩向前，遇罗克先生是我心中的精神支柱，有人说现在的中国灵与肉是分离的，中国人被人嘲讽为“站着的肉”，没有灵魂驱动，而我要说有了遇罗克精神注入，现代青年人的躯体就是有机完整人格力量的体现，遇罗克精神就是理性独立思考彰显，就是灵与肉在当代中国重新结合的驱动力!<br />
&nbsp; &nbsp; &nbsp; &nbsp; 正如乔治&#8226;奥威尔的小说《一九八四》中所说：一切异端当中最大的异端――那便是常识。遇罗克先生和我们现在所处的环境，“常识”的普及依然艰难。<br />
遇罗克先生因言获罪被“红魔”政权屠戮于四十年前的三月五日，四十年后“文字狱”再次重现于今日的“和谐盛世”，平和理性的独立思考者惨遭炼狱之苦，这根本就是宗教裁判所的伎俩，是反智主义的重现。战胜这种野蛮专制最有利的手段之一那便是遇罗克精神―理性独立思考的精神!<br />
让我们在自由之路上“遇”罗克同行!<br />
