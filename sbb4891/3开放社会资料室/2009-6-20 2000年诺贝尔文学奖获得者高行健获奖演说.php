																											<div id="postmessage_133418" class="t_msgfont">文学的理由：2000年诺贝尔文学奖获得者高行健瑞典奖坛演讲<br />
<br />
<br />
我不知道是不是命运把我推上这讲坛，由种种机缘造成的这偶然，不妨称之为<br />
命运。上帝之有无且不去说，面对这不可知，我总心怀敬畏，虽然我一直自认是无<br />
神论者。<br />
&nbsp; &nbsp;一个人不可能成为神，更别说替代上帝，由超人来主宰这个世界，只能把这世<br />
界搅得更乱，更加糟糕。尼采之后的那一个世纪，人为的灾难在人类历史上留下了<br />
最黑暗的纪录。形形色色的超人，号称人民的领袖、国家的元首、民族的统帅，不<br />
惜动用一切暴力手段造成的罪行，绝非是一个极端自恋的哲学家那一番疯话可以比<br />
拟的。我不想滥用这文学的讲坛去奢谈政治和历史，仅仅藉这个机会发出一个作家<br />
纯然个人的声音。<br />
<br />
&nbsp; &nbsp;作家也同样是一个普通人，可能还更为敏感，而过于敏感的人也往往更为脆弱。<br />
一个作家不以人民的代言人或正义的化身说的话，那声音不能不微弱，然而，恰恰<br />
是这种个人的声音倒更为真实。<br />
&nbsp;&nbsp;这里，我想要说的是，文学也□能是个人的声音，而且，从来如此。文学一旦<br />
弄成国家的颂歌、民族的旗帜、政党的喉舌，或阶级与集团的代言，尽管可以动用<br />
传播手段，声势浩大，铺天盖地而来，可这样的文学也就丧失本性，不成其为文学，<br />
 而变成权力和利益的代用品。<br />
&nbsp; &nbsp;这刚刚过去的一个世纪，文学恰恰面临这种不幸，而且较之以往的任何时代，<br />
 留下的政治与权力的烙印更深，作家经受的迫害也更甚。文学要维护自身存在的理<br />
 由而不成为政治的工具，不能不回到个人的声音，也因为文学首先是出自个人的感<br />
&nbsp;&nbsp;受，有感而发。这并不是说文学就一定脱离政治，或是文学就一定干预政治，有关<br />
&nbsp;&nbsp;文学的所谓倾向性或作家的政治倾向，诸如此类的论战也是上一个世纪折腾文学的<br />
&nbsp;&nbsp;一大病痛。与此相关的传统与革新，弄成了保守与革命，把文学的问题统统变成进<br />
 步与反动之争，都是意识形态在作怪。而意识形态一旦同权力结合在一起，变成现<br />
 实的势力，那么文学与个人便一起遭殃。<br />
 <br />
&nbsp;&nbsp;二十世纪的中国文学的劫难之所以一而再，再而三，乃至于弄得一度奄奄一息，<br />
&nbsp;&nbsp;正在于政治主宰文学，而文学革命和革命文学都同样将文学与个人置于死地。以革<br />
命的名义对中国传统文化的讨伐导致公然禁书、烧书。作家被杀害、监禁、流放和<br />
罚以苦役的，这百年来无以计数，中国历史上任何一个帝制朝代都无法与之相比，<br />
弄得中文的文学写作无比艰难，而创作自由更难谈及。<br />
<br />
作家倘若想要赢得思想的自由，除了沉默便是逃亡。而诉诸言语的作家，如果<br />
 长时间无言，也如同自杀。逃避自杀与封杀，还要发出自己个人的声音的作家不能<br />
不逃亡。回顾文学史，从东方到西方莫不如此，从屈原到但丁，到乔伊斯，到托马<br />
斯．曼，到索忍尼辛，到一九年后中国知识分子成批的流亡，这也是诗人和作<br />
家还要保持自己的声音而不可避免的命运。<br />
<br />
在毛泽东实施全面专政的那些年代里，却连逃亡也不可能。曾经蔽护过封建时<br />
代文人的山林寺庙悉尽扫荡，私下偷偷写作得冒生命危险。一个人如果还想保持独<br />
 立思考，只能自言自语，而且得十分隐秘。我应该说，正是在文学做不得的时候我<br />
才充分认识到其所以必要，是文学让人还保持人的意识。<br />
&nbsp;&nbsp;自言自语可以说是文学的起点，藉语言而交流则在其次。人把感受与思考注入<br />
 到语言中，通过书写而诉诸文字，成为文学。当其时，没有任何功利的考虑，甚至<br />
 想不到有朝一日能得以发表，却还要写，也因为从这书写中就已经得到快感，获得<br />
 补偿，有所慰藉。我的长篇小说《灵山》正是在我的那些已严守自我审查的作品却<br />
 还遭到查禁之时著手的，纯然为了排遣内心的寂寞，为自己而写，并不指望有可能<br />
 发表。<br />
 回顾我的写作经历，可以说，文学就其根本乃是人对自身价值的确认，书写其<br />
 时便已得到肯定。文学首先诞生于作者自我满足的需要，有无社会效应则是作品完<br />
 成之后的事，再说，这效应如何也不取决于作者的意愿。<br />
<br />
文学史上不少传世不朽的大作，作家生前都未曾得以发表，如果不在写作之时<br />
从中就已得到对自己的确认，又如何写得下去？中国文学史上最伟大的小说《西游<br />
 记》、《水浒传》、《金瓶梅》和《红楼梦》的作者，这四大才子的生平如今同莎<br />
 士比亚一样尚难查考，□留下了施耐庵的一篇自述，要不是如他所说，聊以自慰，<br />
 又如何能将毕生的精力投入生前无偿的那宏篇钜制？现代小说的发端者卡夫卡和二<br />
 十世纪最深沉的诗人费尔南多．毕索瓦不也如此？他们诉诸语言并非旨在改造这个<br />
 世界，而且深知个人无能为力却还言说，这便是语言拥有的魅力。<br />
<br />
语言乃是人类文明最上乘的结晶，它如此精微，如此难以把握，如此透彻，又<br />
 如此无孔不入，穿透人的感知，把人这感知的主体同对世界的认识联系起来。通过<br />
 书写留下的文字又如此奇妙，令一个个孤立的个人，即使是不同的民族和不同的时<br />
代的人，也能得以沟通。文学书写和阅读的现时性同它拥有的永恒的精神价值也就<br />
 这样联系在一起。<br />
 <br />
我以为，现今一个作家刻意强调某一种民族文化总也有点可疑。就我的出生、<br />
使用的语言而言，中国的文化传统自然在我身上，而文化又总同语言密切相关，从<br />
 而形成感知、思维和表述的某种较为稳定的特殊方式。但作家的创造性恰恰在这种<br />
 语言说过了的地方方才开始，在这种语言尚未充分表述之处加以诉说。作为语言艺<br />
 术的创造者没有必要给自己贴上个现成的一眼可辨认的民族标签。<br />
 <br />
文学作品之超越国界，通过翻译又超越语种，进而越过地域和历史形成的某些<br />
特定的社会习俗和人际关系，深深透出的人性乃是人类普遍相通的。再说，一个当<br />
 今的作家，谁都受过本民族文化之外的多重文化的影响，强调民族文化的特色如果<br />
 不是出于旅游业广告的考虑，不免令人生疑。<br />
 <br />
文学之超越意识形态，超越国界，也超越民族意识，如同个人的存在原本超越<br />
 这样或那样的主义，人的生存状态总也大于对生存的论说与思辨。文学是对人的生<br />
 存困境的普遍关照，没有禁忌。对文学的限定总来自文学之外，政治的，社会的，<br />
 伦理的，习俗的，都企图把文学裁剪到各种框架里，好作为一种装饰。<br />
 <br />
然而，文学既非权力的点缀，也非社会时尚的某种风雅，自有其价值判断，也<br />
 即审美。同人的情感息息相关的审美是文学作品唯一不可免除的判断。诚然，这种<br />
 判断也因人而异，也因为人的情感总出自不同的个人。然而，这种主观的审美判断<br />
 又确有普遍可以认同的标准，人们通过文学薰陶而形成的鉴赏力，从阅读中重新体<br />
 会到作者注入的诗意与美，崇高与可笑，悲悯与怪诞，与幽默与嘲讽，凡此种种。<br />
 <br />
而诗意并非只来自抒情。作家无节制的自恋是一种幼稚病，诚然，初学写作时，<br />
 人人难免。再说，抒情也有许许多多的层次，更高的境界不如冷眼静观。诗意便隐<br />
 藏在这有距离的观注中。而这观注的目光如果也审视作家本人，同样凌驾于书中的<br />
 人物和作者之上，成为作家的第三只眼，一个尽可能中性的目光，那么灾难与人世<br />
 的垃圾便也经得起端详，在勾起痛苦、厌恶与恶心的同时，也唤醒悲悯、对生命的<br />
 爱惜与眷恋之情。<br />
<br />
 植根于人的情感的审美恐怕是不会过时的，虽然文学如同艺术，时髦年年在变。<br />
 然而，文学的价值判断同时尚的区别就在于后者唯新是好，这也是市场的普遍运作<br />
 的机制，书市也不例外。而作家的审美判断倘若也追随市场的行情，则无异于文学<br />
 的自杀。尤其是现今这个号称消费的社会，我以为恰恰得诉诸一种冷的文学。<br />
 <br />
十年前，我结束费时七年写成的《灵山》之后，写了一篇短文，就主张这样一<br />
 种文学：<br />
 <br />
「文学原本同政治无关，只是纯然个人的事情，一番观察，一种对经验的回顾，<br />
 一些臆想和种种感受，某种心态的表达，兼以对思考的满足。」<br />
 <br />
「所谓作家，无非是一个人自己在说话，在写作，他人可听可不听，可读可不<br />
 读，作家既不是为民请命的英雄，也不值得作为偶像来崇拜，更不是罪人或民众的<br />
 敌人，之所以有时竟跟著作品受难，只因为是他人的需要。当权势需要制造几个敌<br />
 人来转移民众注意力的时候，作家便成为一种牺牲品。而更为不幸的是，弄晕了的<br />
 作家竟也以为当祭品是一大光荣。」<br />
<br />
「其实，作家同读者的关系无非是精神上的一种交流，彼此不必见面，不必交<br />
往，只通过作品得以沟通。文学作为人类活动尚免除不了的一种行为，读与写双方<br />
 都自觉自愿。因此，文学对于大众不负有甚么义务。」<br />
 「这种恢复了本性的文学，不妨称之为冷的文学。它所以存在仅仅是人类在追 求物欲满足之外的一种纯粹的精神活动。这种文学自然并非始于今日，只不过以往<br />
主要得抵制政治势力和社会习俗的压迫，现今还要对抗这消费社会商品价值观的浸<br />
 淫，求其生存，首先得自甘寂寞。</div>

							
							
							
							
