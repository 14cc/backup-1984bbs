中华人民共和国迎来了51周年华诞，举国欢腾，全国各族人民共庆这一象征中 华民族彻底推翻三座大山，真正站立起来的伟大日子。然而，在斯堪地那维亚半岛 上的瑞典文学院里，一场闹剧正在上演。一小撮对中国人民怀有极不健康心理的所 谓文学专家，不顾中国人民的强烈反对，将新世纪第一个诺贝尔文学奖颁给了现居 法国的华裔“作家”高行健。瑞典文学院的倒行逆施，极大地伤害了中华民族的感 情，这是对12亿中国人民的严重挑衅。 <br />
