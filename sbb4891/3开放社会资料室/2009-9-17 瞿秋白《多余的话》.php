<br />
“知我者，谓我心忧；不知我者，谓我何求。”何必说？ <br />
――代序 <br />
<br />
话既然是多余的，又何必说呢？已经是走到了生命的尽期，余剩的日子，不但不能按照年份来算，甚至不能按星期来算了。就是有话，也是可说可不说的了。 <br />
<br />
但是，不幸我卷入了“历史的纠葛”――直到现在，外间好些人还以为我是怎样怎样的。我不怕人家责备、归罪，我倒怕人家“钦佩”。但愿以后的青年不要学我的样子，不要以为我以前写的东西是代表什么主义的。所以我愿意趁这余剩的生命还没有结束的时候，写一点最后的最坦白的话。 <br />
<br />
而且，因为“历史的误会”，我十五年来勉强做着政治工作。――正因为勉强，所以也永远做不好，手里做着这个，心里想着那个。在当时是形格势禁，没有余暇和可能说一说我自己的心思，而且时刻得扮演一定的角色。现在我已经完全被解除了武装，被拉出了队伍，只剩得我自己了，心上有不能自已的冲动和需要。说一说内心的话，彻底暴露内心的真相。布尔什维克所讨厌的小资产阶级知识者的自我分析的脾气，不能够不发作了。 <br />
<br />
虽然我明知道这里所写的，未必能够到得读者手里，也未必有出版的价值，但是，我还是写一写罢。人往往喜欢谈天，有时候不管听的人是谁，能够乱谈几句，心上也就痛快了。何况我是在绝灭的前夜，这是我最后“谈天”的机会呢！ <br />
<br />
（一九三五・五・一七于汀州狱中）<br />
