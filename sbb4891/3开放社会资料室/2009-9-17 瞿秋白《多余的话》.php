　　我在母亲自杀家庭离散之后，孑然一身跑到北京，只愿能够考进北大，研究中国文学，将来做个教员度过这一世。什麽“治国平天下”的大志都是没有的，坏在“读书种子”爱书本子，爱文艺，不能安分守己地专心于升官发财。到了北京之后，住在堂兄纯白家里，北大的学膳费也希望他能够帮助我――他却没有这种可能，叫我去考普通文官考试，又没有考上，结果，是挑选一个既不要学费又有“出身”的外交部立俄文专修馆去进。这样，我就开始学俄文（一九一七年夏），当时并不知道俄国已经革命，也不知道俄国文学的伟大意义，不过当作将来谋一碗饭吃的本事罢了。<br />
<br />
　　一九一八年开始看了许多新杂志，思想上似乎有相当的进展，新的人生观正在形成。可是，根据我的性格，所形成的与其说是革命思想，无宁说是厌世主义的理智化。所以最早我国郑振铎、瞿世英、耿济之几个朋友组织《新社会》杂志的时候，我是一个近于托尔斯泰派的无政府主义者，而且，根本上我不是一个“政治动物”。五四运动期间，只有极短期的政治活动。不久，因为已经能够查着字典看俄国文学名著，我的注意力就大部分放在文艺方面了。对于政治上的各种主义，都不过略略“涉猎”求得一些现代常识，并没有兴趣去详细研究。然而可以说，这时就开始“历史的误会”了：事情是这样的――五四运动一开始，我就当了俄文专修的总代表之一。<br />
<br />
　　当时的一些同学里，谁也不愿意干，结果，我得做这一学校的“政治领袖”，我得组织同学群众去参加当时的政治运动。不久，李大钊，张崧年他们发起马克思主义研究会（或是“俄罗斯研究会”罢？），我也因为读了俄文的倍倍尔的《妇女与社会》的某几段，对于社会――尤其是社会主义最终理想发生了好奇心和研究的兴趣，所以也加入了。这时候大概是一九一九年底一九二○年初，学生运动正在转变和分化，学生会的工作也没有以前那麽热烈了。我就多读一些书。<br />
<br />
　　最后，有了机会到俄国去了――北京晨报要派通信记者到莫斯科去，来找我。我想，看一看那“新国家”，尤其是借此机会把俄国文学好好研究一下，的确是一件最惬意的事，于是就动身去（一九二○年八月）。<br />
<br />
　　最初，的确吃了几个月的黑面包，饿了好些时候。后来俄国国内战争停止，新经济政策实行，生活也就宽裕了些。我在这几个月内请了私人教授，研究俄文、俄国史、俄国文学史；同时，为着应付晨报的通信，也很用心看俄国共产党的报纸、文件，调查一些革命事迹。我当时对于共产主义只有同情和相当的了解，并没有想到要加入共产党，更没有心思要自己来做中国共产党的“创始人”。因为那时候，我误会着加入了党就不能专修文学――学文学仿佛就是不革命的观念，在当时已经通行了。<br />
<br />
　　可是，在当时的莫斯科，除我以外，一个俄文翻译都找不到。因此，东方大学开办中国班的时候（一九二一年秋），我就当了东大的翻译和助教；因为职务的关系，对马克思主义的理论书籍不得不研究些，而文艺反而看得少了。不久（一九二二年底），陈独秀代表中国共产党到莫斯科（那时我已经是共产党员，还是张太雷介绍我进党的），我就当他的翻译。独秀回国的时候，他要我回去工作，我就同了他回到北京。于右任、邓中夏等创辨“上海大学”的时候，我正在上海。这是一九二二年夏天。他们请我当上大的教务长兼社会学系主任。那时，我在党内只兼着一点宣传工作，编辑《新青年》。<br />
<br />
　　上大初期，我还有余暇研究一些文艺问题，到了国民党改组，我来往上海广州之间，当翻译，参加一些国民党的工作（例如上海的国民党中央执行部委员等）；而一九二五年一月共产党第四次全国代表大会，又选举了我的中央委员。这时候，就简直完全只能做政治工作了。我的肺病又不时发作，更没有可能从事我所爱好的文艺。虽然我当时对政治问题还有相当的兴趣，可是有时还会怀念着文艺而“怅然若失”的。<br />
<br />
　　武汉时代的前夜（一九二七年初），我正从重病之中脱险。将近病好的时候，陈独秀、彭述之等的政治主张，逐渐暴露机会主义的实质，一般党员对他们失掉了信仰。在中国共产党第五次大会上（一九二七年四五月间），独秀虽然仍旧被选，但是对于党的领导已经不大行了。武汉的国共分裂之后，独秀就退出中央。那时候，没有别人主持，就轮到我主持中央政治局。其实，我虽然在一九二六年年底及一九二七年年初就发表了一些议论反对彭述之，随后不得不反对陈独秀，可是，我根本上不愿意自己来代替他们――至少是独秀。我确是一种调和派的见解。当时只想独秀能够纠正他的错误观念，不听述之的理论。等到实逼处此，要我“取独秀而代之”，我一开始就觉得非常之“不合式”，但是，又没有什麽别的辨法。这样我担负了直接的政治领导的一年光景（一九二七年七月到一九二八年五月）。这期间发生了南昌暴动，广州暴动，以及最早的秋收暴动。当时，我的领导在方式上同独秀时代不同了。独秀是事无大小都参加和主持的。我却因为对组织尤其是军事非常不明了，也毫无兴趣，所以只发表一般政治主张，其余调遣人员和实行的具体计划等，就完全听组织部军事部去办。那时自己就感觉到空谈无聊，但是一转念要退出领导地位，又感到好像是拆台。这样，勉强着度过了这一时期。<br />
<br />
　　一九二八年六月间共产党开第六次大会的时候，许多同志反对我，也有许多同志赞成我。我的进退成为党的政治主张的联带问题。所以，我虽然屡次想说“你们饶了我吧，我实在没有兴趣和能力负担这个领导工作了”。但是，终于没有说出口。当时形格势禁，旧干部中又没有别人，新干部起来领导的形势还没有成熟，我只得仍旧担着这个名义。可是，事实上“六大”之后，中国共产党的直接领导者是李立三和向忠发等等。因为他们在国内主持实际工作，而我在莫斯科当代表当了两年。直到立三的政治路线走上了错误的道路，我回到上海开三中全会（一九三○年九月底），我更觉得自己的政治能力确实非常薄弱，竟辨别不出立三的错误程度。结果，中央不得不再招集会议――就是四中全会，来开除立三的中央委员、我的政治局委员，新干部起来接替了政治的最高领导。我当时觉得松了一口气。从一九二五年到一九三一年初，整整五年。我居然当了中国共产党领袖之一，最后三年甚至仿佛是最主要的领袖（不过并没有象外间传说的“总书记”的名义）。<br />
<br />
　　我自己忖度着，像我这样的性格、才能、学识，当中国共产党的领袖确实是一个“历史的误会”。我本是一个半吊子的“文人”而已，直到最后还是“文人积习未除”的。对于政治，从一九二七年起就逐渐减少兴趣。到最近一年――在瑞金的一年实在完全没有兴趣了。工作是“但求无过”的态度，全国的政治情形实在懒得问。一方面固然是身体衰弱，精力短少，而表现十二分疲劳的状态；别的方面也是几十年为着“顾全大局”勉强负担一时的政治翻译、政治工作，而一直拖延下来，实在违反我的兴趣和性情的结果。这真是十几年的一场误会，一场噩梦。<br />
<br />
　　我写这些话，绝不是要脱卸什麽责任――客观上我对共产党或是国民党的“党国”应当担负什麽责任，我决不推托，也决不能用我主观的情绪来加以原谅或者减轻。我不过想把我的真情，在死之前，说出来罢了。总之，我其实是一个很平凡的文人，竟虚负了某某党的领袖的名声十来年，这不是“历史的误会”，是什麽呢？<br />
<br />
脆弱的二元人物<br />
<br />
　　一只赢弱的马拖着几千斤的辎重车，走上了险峻的山坡，一步步地往上爬，要往后退是不可能，要再往前去是实在不能胜任了。我在负责政治领导的时期，就是这样一种感觉。欲罢不能的疲劳使我永久感觉一种无可形容的重压。精神上政治上的倦怠，使我渴望“甜蜜的”休息，以致于脑筋麻木，停止一切种种思想。一九三一年一月的共产党四中全会开除了我的政治局委员之后，我的精神状态的确是“心中空无所有”的情形，直到现在还是如此。<br />
<br />
　　我不过三十六岁（虽然照阴历的习惯我今年是三十八岁），但是，自己觉得已经非常地衰惫，丝毫青年壮年的兴趣都没有了。不但一般的政治问题懒得去思索，就是一切娱乐，甚至风景都是漠不相关的了。本来我从一九一九年就得了吐血病，一直没有好好医治的机会。肺结核的发展曾经在一九二六年走到非常危险的阶段，那年幸而勉强医好了。可是立即赶到武汉去，立即又是半年最忙碌紧张的工作。虽然现在肺痨的最危险期逃过了，而身体根本弄坏了，虚弱得简直是一个废人。从一九二○年直到一九三一年初，整整十年――除却躺在床上不能行动神志昏瞀的几天以外――我的脑筋从没有得到休息的日子。在负责时期，神经的紧张自然是很厉害的，往往十天八天连续的不安眠，为着写一篇政治论文或者报告。这继续十几年的不休息，也许是我精神疲劳和十分厉害的神经衰弱的原因，然而究竟我离衰老时期还很远。这十几年的辛劳，确实算起来，也不能说怎麽了不得，而我竟成了颓丧残废的废人了。我是多麽脆弱，多麽不禁磨练呵！<br />
<br />
　　或者，这不尽是身体本来不强壮，所谓“先天不足”的原因罢。<br />
<br />
　　我虽然到了十三、四岁的时候就很贫苦了，可是我的家庭，世代是所谓“衣租食税”的绅士阶级，世代读书，也世代做官。我五、六岁的时候，我的叔祖瞿庚韶，还在湖北布政使任上。他死的时候，正署理湖北巡抚。因此，我家的田地房屋虽然几十年前就已经完全卖尽，而我小时候，却靠着叔祖伯父的官俸过了好几年十足的少爷生活。绅士的体面“必须”维持。我母亲宁可自杀而求得我们兄弟继续读书的可能；而且我母亲因为穷而自杀的时候，家里往往没有米煮饭的时候，我们还用着一个仆妇（积欠了她几个月的工资，到现在还没有还清）。我们从没有亲手洗过衣服，烧过一次饭。<br />
<br />
　　直到那样的时候，为着要穿长衫，在母亲死后，还剩下四十多元的裁缝债，要用残余的木器去抵帐。我的绅士意识――就算是深深潜伏着表面不容易察觉罢――其实是始终没脱掉的。<br />
<br />
　　同时，我二十一、二岁，正当所谓人生观形成的时期，理智方面是从托而斯泰式的无政府主义很快就转到了马克思主义。人生观或是主义，这是一种思想方法――所谓思路；既然走上了这条道路，却不是轻易就能改换的。而马克思主义是什麽？是无产阶级的宇宙观和人生观。这同我潜伏的绅士意识、中国式的士大夫意识、以及后来蜕变出来的小资产阶级或者市侩式的意识，完全处于敌对的地位。没落的中国绅士阶级意识之中，有些这样的成分：例如假惺惺的仁慈礼让、避免斗争……以致寄生虫式的隐士思想。（完全破产的绅士往往变成城市的波希美亚――高等游民，颓废的、脆弱的、浪漫的，甚至狂妄的人物。说得实在些，是废物。我想，这两种意识在我内心里不断地斗争，也就侵蚀了我极大部分的精力。我得时时刻刻压制自己绅士和游民式的情感，极勉强地用我所学到的马克思主义的理智来创造新的情感、新的感觉方法。可是无产阶级意识在我的内心里是始终没有得到真正的胜利的。）<br />
<br />
　　当我出席政治会议，我就会“就事论事”，抛开我自己的“感觉”专就我所知道的那一点理论去推断一个问题，决定一种政策等等。但是，我一直觉得这工作是“替别人做的”。我每次开会或者做文章的时候，都觉得很麻烦，总在急急于结束，好“回到自己那里去”休息。我每每幻想着：我愿意到随便一个小市镇去当一个教员，并不是为着发展什麽教育，只不过求得一口饱饭罢了。在余的时候，读读自己所爱读的书、文艺、小说、诗词、歌曲之类，这不是很逍遥的吗？<br />
<br />
　　这种两元化的人格，我自己早已发觉――到去年更是完完全全了解了，已经不能丝毫自欺的了；但是“八七”会议之后，我并没有公开地说出来，四中全会之后也没有说出来，在去年我还是决断不下，以致延迟下来，隐忍着，甚至对之华（我的爱人）也只偶然露一点口风，往往还要加一番弥缝的话。没有这样的勇气。<br />
<br />
　　可是真相是始终要暴露的，“二元”之中总有“一元”要取得实际上的胜利。正因为我的政治上疲劳倦怠，内心的思想斗争不能再持续了。老实说，在四中全会之后，我早已成为十足的市侩――对于政治问题我竭力避免发表意见。中央怎麽说，我就怎麽说，认为我说错了，我立刻承认错误，也没有什麽心思去辩白。说我是机会主义就是机会主义好了，一切工作只要交代得过去就算了。我对于政治和党的种种问题，真没有兴趣去注意和研究。只因为六年的“文字因缘”，对于现代文学以及文学史上的各种有趣的问题，有时候还有点兴趣去思考一下，然而大半也是欣赏的分数居多，而研究分析的分数较少。而且体力的衰弱也不容许我多所思索了。<br />
<br />
　　体力上的感觉是：每天只要用脑到两三小时以上，就觉得十分疲劳，或者过分的畸形的兴奋――无所谓的兴奋，以致于不能睡觉，脑痛……冷汗。<br />
<br />
　　唉，脆弱的人呵！所谓无产阶级的革命队伍需要这种东西吗？！我想，假定我保存这多余的生命若干时候，我另有拒绝用脑的一个方法，我只做些不用自出心裁的文字工作，“以度余年”。但是，最后也是趁早结束了罢。<br />
<br />
我和马克思主义<br />
<br />
　　当我开始我的社会生活的时候，正是中国的“新文化”运动的浪潮非常汹涌的时期。为着继续深入地研究俄文和俄国文学，我刚好又不能不到世界第一个“马克思主义的国家”去。我那时的思想是很紊乱的：十六、七岁开始读了些老庄之类的子书，随后是宋儒语录，随后是佛经、《大乘起星信论》――直到胡适之的《哲学史大纲》、梁漱溟的《印度哲学》，还有当时出版的一些科学理论、文艺评论。在到俄国之前，固然已经读过倍倍尔的著作，《共产党宣言》之类，极少几本马克思的书籍，然而对马克思主义的认识是根本说不上的。<br />
<br />
　　而且，我很小的时候，就不知怎样有一个古怪的想头：为什麽每一个读书人都要去“治国平天下”呢？个人找一种学问或是文艺研究一下不好吗？所以我到俄国之后，虽然因为职务的关系，时常得读些列宁他们的著作、论文、演讲，可是这不过求得对于俄国革命和国际形势的常识，并没有认真去研究。政治上一切种种主义，正是“治国平天下”的各种不同的脉案和药方。<br />
<br />
　　我根本不想做“王者之师”，不想做“诸葛亮”――这些事自然有别人去干――我也就不去研究了。不过，我对于社会主义或共产主义的终极理想，却比较有兴趣。<br />
<br />
　　记得当时懂得了马克思主义的共产社会同样是无阶级、无政府、无国家的最自由的社会，我心上就很安慰了，因为这同我当初无政府主义、和平博爱世界的幻想没有冲突了。所不同的是手段。马克思主义告诉我要达到这样的最终目的，客观上无论如何也逃不了最尖锐的阶级斗争，以致无产阶级专政――也就是无产阶级统治国家的一个阶段。为着要消灭“国家”，一定要先组织一时期的新式国家；为着要实现最彻底的民权主义（也就是所谓的民权的社会），一定要先实行无产阶级的民权。这表面上“自相矛盾”，而实际上很有道理的逻辑――马克思主义所谓辩证法――使我很觉得有趣。我大致了解了这问题，就搁下了，专心去研究俄文，至少有大半年，我没有功夫去管什麽主义不主义。<br />
<br />
　　后来，莫斯科东方大学要我当翻译，才没的办法又打起精神去看那一些书。谁知越到后来就越没有功夫继续研究文学，不久就喧宾夺主了。<br />
<br />
　　但是，我第一次在俄国不过两年，真正用功研究马克思主义的常识不过半年，这是随着东大课程上的需要看一些书。明天要译经济学上的那一段，今天晚上先看一道，作为预备。其它唯物史观哲学等等也是如此。这绝不是有系统的研究。至于第二次我到俄国（一九二八年――一九三○年），那时当着共产党的代表，每天开会，解决问题，忙个不了，更没有功夫做有系统的学术上的研究。<br />
<br />
　　马克思主义的主要部分：唯物论的哲学。唯物史观――阶级斗争的理论，以及经济政治学，我都没有系统地研究过。资本论――我就根本没有读过，尤其对于经济学我没有兴趣。我的一点马克思主义理论的常识，差不多都是从报章杂志上的零星论文和列宁几本小册子上得来的。<br />
<br />
　　可是，在一九二三年的中国，研究马克思主义以至一般社会学的人，还少得很。因此，仅仅因此，我担任了上海大学社会学系教授之后，就逐渐地偷到所谓“马克思主义理论家”的虚名。<br />
<br />
　　其实，我对这些学问，的确只知道一点皮毛。当时我只是根据几本外国文的书籍转译一下，编了一些讲义。现在看起来，是十分幼稚、错误百出的东西。现在有许多新进的青年，许多比较有系统地研究了马克思主义的学者――而且国际的马克思主义的学术水平也提高了许多。<br />
<br />
　　还有一个更重要的“误会”，就是用马克思主义来研究中国的现代社会，部分的是研究中国历史的发端――也不得不由我来开始尝试。五四以后的五年中间，记得只有陈独秀、戴季陶、李汉俊几个人写过几篇关于这个问题的论文，可是都是无关重要的。我回国之后，因为已经在党内工作，虽然只有一知半解的马克思主义知识，却不由我不开始这个尝试：分析中国资本主义关系的发展程度，分析中国社会阶级分化的性质，阶级斗争的形势，阶级斗争和反帝国主义的民族解放运动的关系等等。<br />
<br />
　　从一九二三年到一九二七年，我在这方面的工作，自然，在全党同志的督促，实际斗争的反映，以及国际的领导之下，逐渐有相当的进步。这决不是我一个人的工作，越到后来，我的参加越少。单就我的“成绩”而论，现在所有的马克思主义者都可明显地看见，我在当时所做的理论上的错误，共产党怎样纠正了我的错误，以及我的理论之中包含着多麽混杂和小资产阶级机会主义的成分。<br />
<br />
　　这些机会主义的成分发展起来，就形成错误的政治路线，以致于中国共产党中央委员会不能不开除我的政治局委员。的确，到一九三○年，我虽然在国际上参加了两年的政治工作，相当得到一些新的知识，受到一些政治上的锻炼，但是，不但不进步，自己反而觉得退步了。中国的阶级斗争早已进到了更高的阶段，对于中国的社会关系和政治形势，需要更深刻更复杂的分析，更明了的判断，而我的那点知识绝对不够，而且非无产阶级的反布尔塞维克的意识就完全暴露了。<br />
<br />
　　当时，我逐渐觉得许多问题，不但想不通，甚至不想动了。新的领导者发挥某些问题议论之后，我会感觉到松快，觉得这样解决原是最适当不过的，我当初为什麽简直想不通；但是――也有时候会觉得不了解。<br />
<br />
　　此后，我勉强自己去想一切“治国平天下”的大问题的必要，已经没有了！我在十二分疲劳和吐血症复发的期间，就不再去“独立思索”了。一九三一年初，就开始我政治上以及政治思想上的消极时期，直到现在。从那时候起，我没有自己的思想。（我以中央的思想为思想。）这并不是说我是一个很好的模范党员，对于中央的理论政策都完全而深刻的了解。相反的，我正是一个最坏的党员，早就值得开除的，因为我对中央的理论政策不加思索了。偶尔我也有对中央政策怀疑的时候，但是，立刻就停止怀疑了――因为怀疑也是一种思索；我既然不思索了――自然也就不怀疑。<br />
<br />
　　我的一知半解的马克思主义知识，曾经在当时起过一些作用――好的坏的影响都是人所共知的事情，不用我自己来判断――而到了现在，我已经在政治上死灭，不再是一个马克思主义的宣传者了。<br />
<br />
　　同时要说我已放弃了马克思主义，也是不确的。如果要同我谈起一切种种政治问题，我除开根据我那一点一知半解的马克思主义的方法来推论以外，却又没有什麽别的方法。事实上我这些推论又恐怕包含着许多机会主义，也就是反马克思列宁主义的观点在内，这是“亦未可知”的。<br />
<br />
　　因此，我更不必狂然费力去思索：我的思路已经在青年时期走上了马克思主义的初步，无从改变；同时，这思路却同非马克思主义的岐路交错着，再自由任意地走去，不知会跑到什麽地方去。――而最主要的是我没勇气再跑了，我根本没有精力在作政治的社会科学的思索了。<br />
<br />
盲动主义和立三路线<br />
<br />
　　当我不得不负担中国共产党的政治领导的时候，正是中国革命进到了最巨大的转变和震荡的时代，这就是武汉时代结束之后。分析新的形势，确定新的政策，在中国民族解放运动和阶级斗争最复杂最剧烈的路线汇合分化转变的时期，这是一个非常艰巨的任务。当时，许多同志和我，多多少少都犯了政治上的错误；同时，更有许多以前的同志在这阶级斗争进一步的关口，自觉或不自觉的离开了革命队伍。在最初，我们在党的领导之下所决定的政策一般的是不正确的。武汉分裂以后，我们接着就决定贺叶的南昌暴动和两湖广东暴动（一九二七年），到十一月又决定广州暴动。这些暴动本身并不是什么盲动主义，因为都有相当的群众基础。固然，中国的一般革命形势，从一九二七年三月底英美日帝国主义炮轰南京威胁国民党反共以后，就已经开始低落；但是，接着而来的武汉政府中的奋斗、分裂――直到广州暴动的举出苏维埃旗帜，都还是革命势力方面正当的挽回局势的尝试，结果，是失败了――就是说没有能够把革命形势重新转变到高涨的阵容，必须另起炉灶。而我――这时期当然我应当负主要的责任――在一九二八年初，广州暴动失败之后，仍旧认为革命形势一般的存在，而且继续高涨，这就是盲动主义的路线了。<br />
<br />
　　原本个别的盲动现象，我们和当时的中央从一九二七年十月起就表示反对；对于有些党部不努力去领导和争取群众，反而孤注一掷，或者仅仅去暗杀豪绅之类的行动，我们总是加以纠正的。可是，因为当时整个路线错误，所以不管主观上怎样了解盲动主义现象不好，费力于枝枝节节的纠正，客观上却在领导着盲动主义的发展。<br />
<br />
　　中国共产党第六次大会纠正了这个错误，使政策走上了正确的道路。自然，武汉时代之后，我们所得到的中国革命之中的最重要的教训：例如革命有一省或几省先胜利的可能和前途，反帝国主义革命最密切的和土地革命联系着等――都是“六大”所采纳的。苏维埃革命的方针，就在“六大”更明确地规定下来。<br />
<br />
　　但是以我个人而论，在那个时候，我的观点之中不仅有过分估量革命形势的发展，以致助长盲动主义的错误。对于中国农民阶层的分析，认为富农还在革命战线之内，认为不久的将来就可以在某些大城市取得暴动的胜利等观点，也已经潜伏着或者有所表示。不过，同志们都没有发觉这些观点的错误，还没有指出来。我自己当然更不会知道这些是错误的。直到一九二九年秋天，讨论农民问题的时侯，才开始暴露我在农民问题上的错误。不幸得很，当时没有更深刻更无情的揭发……。<br />
<br />
　　此后，就来了立三路线的问题了。<br />
<br />
　　一九二九年底，我还在莫斯科的时侯，就听说立三和忠发的政策有许多不妥当的地方。同时，莫斯科中国劳动大学(前称孙中山大学)的学生中间发生非常剧烈的斗争。我向来没有知人之明，只想弥缝缓和这些斗争，觉得互相攻讦批评的许多同志都是好的，听他们所说的事情却往往有些非常出奇，似乎都是故意夸大事实，奉为“打倒”对方的理由。因此，我就站在调和的立场。这使得那里的党部认为我恰好是机会主义和异己分子的庇护者。结果，撤销了我的中国共产党驻莫斯科代表的职务，准备回国。自然，在回国任务之中，最主要的是纠正立三的错误，消灭莫斯科中国同志的派别观念对于国内同志的影响。<br />
<br />
　　但是，事实上我什么也没做到。立三的错误在那时――一九三○年夏天――已经形成了自己的半托洛茨基的路线，派别观念也使得党内到处压抑莫斯科回国的新干部。而我回来召集的三中全会，以及中央一切处置，都只是零零碎碎地纠正了立三的一些显而易见的错误。既没有指出立三的路线错误，更没有在组织上和一切计划及实际工作上保证国际路线的执行。实际上我的确没有认出立三路线和国际路线的根本不同。<br />
<br />
　　老实说，立三路线是我的许多错误观点――有人说是瞿秋白主义――逻辑的发展。立三的错误政策可以说是一种失败主义。他表面上认为中国全国的革命胜利的局面已经到来，这会推动全世界的成功，其实是觉得自己没有把握和发展苏维埃革命在几个县区的胜利，革命前途不是立即向大城市发展而取得全国胜利以至全世界的胜利，就是迅速的败亡，所以要孤注一掷地拼命。这是用左倾空谈来掩盖右倾机会主义的实质。因此在组织上，在实际上，在土地革命的理论上，在工会运动的方针上，在青年运动和青年组织等等各种问题上……无往而不错。我在当时却辨别不出来。事后我曾说，假定“六大”之后，留在中国直接领导的不是立三而是我，那么，在实际上我也会走到这样的错误路线，不过不至于象立三这样鲁莽，也可以说，不会有立三那样的勇气。我当然间接地负着立三路线的责任。<br />
<br />
　　于是四中全会后，就决定了开除立三的中央委员，开除我的政治局委员。我呢，象上面已经说过的，正感谢这一开除，使我卸除了千均万担。我第二次回国是一九三○年八月中旬，到一九三一年一月七日，我就离开了中央政治领导机关。这期间只有半年不到的时间。可是这半年时间对于我几乎比五十年还长！人的精力已经完全用尽了似的，我请了长假休息医病――事实上从此脱离了政治舞台。<br />
<br />
　　再想回头来干一些别的事情，例如文艺的译著等，已经觉得太迟了。从一九二○年到一九三○年，整整十年我离开了“自己的家”――我所愿意干的俄国文学的研究――到这时候方回来，不但田园荒芜，而且自己的力气也已经衰惫了。自然，有可能还是干一干，“以度余年”的。可是接着就是大病，时发时止，耗费了三年的光阴。一九三四年一月，为着在上海养病的不可能，又跑到瑞金――到瑞金已是二月五日了――担任了人民委员的消闲职务。可是，既然在苏维埃中央担负了一部分的工作，虽然不用出席党的中央会议，不必参与一切政策的最初议论和决定，然而要完全不问政治又办不到了。我就在敷衍塞责、厌倦着政治却又不得不略微问一问政治的状态中间，过了一年。<br />
<br />
　　最后这四年中间，我似乎记得还做了几次政治问题上的错误。但是现在我连内容都记不清楚了，大概总是我的老机会主义发作罢了。我自己不愿意有什么和中央不同的政见。我总是立刻“放弃”这些错误的见解，其实我连想也没有仔细想，不过觉得争辩起来太麻烦了，既然无关紧要，就算了吧。<br />
<br />
　　我的政治生命其实早已结束了。<br />
<br />
　　最后这四年，还能说我继续在为马克思主义奋斗，为苏维埃奋斗，为站着党的正确路线奋斗吗？例行公事办了些，说“奋斗”是太恭维了。以前几年的盲动主义和立三路线的责任，都决不应当因此而减轻的；相反，在共产党的观点上来看，这个责任倒是更加加重了。历史的事实是抹煞不了的，我愿意受历史的最公平的裁判 ！　　　<br />
<br />
　　（一九三五・五・二十）<br />
<br />
“文人”<br />
<br />
　　“一为文人，便无足观”，――这是清朝一个汉学家说的。的确，所谓“文人”正是无用的人物。这并不是现代意义的文学家、作家或是文艺评论家，这是吟风弄月的“名士”，或者是……说简单些，读书的高等游民。他什么都懂的一点，可是一点没有真实的知识。正因为他对于当代学术水平以上的各种学问都有少许的常识，所以他自以为是学术界的人。可是，他对任何一种学问都没有系统的研究、真正的心得，所以他对于学术是不会有什么贡献的，对于文艺也不会有什么成就的。<br />
<br />
　　自然，文人也有各种各样不同的典型，但是大都实际上是高等游民罢了。假如你是一个医生，或是工程师，化学技师……真正的作家，你自己会感觉到每天生活的价值，你能够创造或是修补一点什么，只要你愿意。就算你是一个真正的政治家罢，你可以做错误。你可以坚持你的错误，但是也会认真地为着自己的见解去斗争、实行。只有文人就没有希望了，他往往连自己也不知道究竟做的是什么！<br />
<br />
　　“文人”是中国中世纪的残余和“遗产”――一份很坏的遗产。我相信，再过十年八年没有这一种知识分子了。<br />
<br />
　　不幸，我自己不能够否认自己正是“文人”之中的一种。<br />
<br />
　　固然，中国的旧书，十三经、二十四史、子书、笔记、丛书、诗词曲等，我都看过一些，但是我是找到就看，忽然想起就看，没有什么研究的。一些科学论文，马克思主义的非马克思主义的，我也看过一些，虽然很少。所以这些新新旧旧的书对于我，与其说是知识的来源，不如说是清闲的工具。究竟在哪一种学问上，我有点真实的知识？我自己是回答不出的。<br />
<br />
　　可笑的很，我做过所谓“杀人放火”的共产党的领袖？可是，我确是一个最懦怯的“婆婆妈妈”的书生，杀一只老鼠都不会的，不敢的。<br />
<br />
　　但是，真正的懦怯不在这里。首先是差不多完全没有自信力，每一个见解都是动摇的，站不稳的。总希望有一个依靠。记得布哈林初次和我谈话的时侯，说过这么一句俏皮话：“你怎么和三层楼上的小姐一样，总那么客气，说起话来，不是‘或是’，就是‘也许’、‘也难说’……等”。其实，这倒是真心话。可惜的是人家往往把我的坦白当作“客气”或者“狡猾”。<br />
<br />
　　我向来没有为着自己的见解而奋斗的勇气，同时，也很久没有承认自己错误的勇气。当一种意见发表之后，看看没有有力的赞助，立刻就怀疑起来；但是，如果没有另外的意见来代替，那就只会照着这个自己也怀疑的意见做去。看见一种不大好的现象，或是不正确的见解，却没有人出来指摘，甚至其势汹汹的大家认为这是很好的事情，我也始终没有勇气说出自己的怀疑来。优柔寡断，随波逐流，是这种“文人”必然性格。<br />
<br />
　　虽然人家看见我参加过几次大的辩论，有时侯仿佛很激烈，其实我是很怕争论的。我向来觉得对方说的话“也对”，“也有几分理由”， “站在对方的观点上他当然是对的”。我似乎很懂得孔夫子忠恕之道。所以我毕竟做了“调和派”的领袖。假使我激烈的辩论，那么，不是认为“既然站在布尔塞维克的队伍里就不应当调和”，因此勉强着自己，就是没有抛开“体面”立刻承认错误的勇气，或者是对方的话太幼稚了，使我“箭在弦上不得不发”。<br />
<br />
　　其实，最理想的世界是大家不要争论，“和和气气的过日子”。<br />
<br />
　　我有许多标本的“弱者的道德”――忍耐，躲避讲和气，希望大家安静些，仁慈些等等。固然从少年时候起，我就憎恶贪污、卑鄙……以致一切恶浊的社会现象，但是我从来没有想做侠客。我只愿意自己不做那些罪恶。有可能呢，去劝劝他们不要在那样做；没有可能呢，让他们去罢，他们也有他们的不得已的苦衷罢！<br />
<br />
　　我的根本性格，我想，不但不足以锻炼成布尔什维克的战士，甚至不配做一个起码的革命者。仅仅为着“体面”，所以既然卷进了这个队伍，也就没有勇气自己认识自己，而请他们把我洗刷出来。<br />
<br />
　　但是我想，如果叫我做一个“戏子”――舞台上的演员，到很会有些成绩，因为十几年我一直觉得自己一直在扮演一定的角色。扮着大学教授，扮着政治家，也会真正忘记自己而完全成为“剧中人”。虽然，这对于我很痛苦，得每天盼望着散会，盼望同我谈政治的朋友走开，让我卸下戏装，还我本来面目 ――躺在床上去，极疲乏的念着：“回‘家’去罢，回‘家’去罢！”这的确是很苦的――然而在舞台上的时候，大致总还扮的不差，象煞有介事的。<br />
<br />
　　为什么？因为青年精力比较旺盛的时候，一点游戏和做事的兴总会有的。即时不是你自己的事，当你把他做好的时候，你也感觉到一时的愉快。譬如你有点小聪明，你会摆好几幅“七巧版图”或者“益智图”，你当时一定觉得痛快，正象在中学校的时候，你算出几个代数难题似的，虽然你并不预备做数学家。<br />
<br />
　　不过，扮演舞台上的角色究竟不是“自己的生活”，精力消耗在这里，甚至完全用尽，始终是后悔也来不及的事情。等到精力衰惫的时侯，对于政治的舞台，实在是十分厌倦了。<br />
<br />
　　庞杂而无秩序的一些书本上的知识和累赘而反乎自己兴趣的政治生活，使我麻木起来，感觉生活的乏味。<br />
<br />
　　本来，书生对于宇宙间的一切现象，都不会有亲切的了解，往往会把自己变成一大堆抽象名词的化身。一切都有一个“名词”，但是没有实感。譬如说，劳动者的生活、剥削、斗争精神、土地革命、政权等……一直到春花秋月、崦嵫、委蛇，一切种种名词、概念、词藻，说是会说的，等到追问你究竟是怎么一回事，那就会感觉到模糊起来。<br />
<br />
　　对于实际生活，总象雾里看花似的，隔着一层膜。<br />
<br />
　　“文人”和书生大致没有任何一种具体的知识。他样样都懂得一点，其实样样都是外行。要他开口议论一些“国家大事”，在不太复杂和具体的时侯，他也许会。但是，叫他修理一辆汽车，或者配一剂药方，办一个合作社，买一批货物，或者清理一本帐目，再不然，叫他办好一个学校……总之，无论哪一件具体而切实的事情 ，他都会觉得没有把握的。<br />
<br />
　　例如，最近一年来，叫我办苏维埃的教育。固然，在瑞金、宁都、兴国这一带的所谓“中央苏区”，原来是文化落后的地方，譬如一张白纸，在刚刚着手办教育的时侯，只是办义务小学校，开办几个师范学校(这些都做了)。但是，自己仔细想 一想，对于这些小学校和师范学校，小学教育和儿童教育的特殊问题，尤其是国内战争中工农群众教育的特殊问题，都实在没有相当的知识，甚至普通常识都不够 ！<br />
<br />
　　近年来，感觉到这一切种种，很愿意“回过去再生活一遍”。<br />
<br />
　　雾里看花的隔膜的感觉，使人觉得异常地苦闷、寂寞和孤独，很想仔细地亲切地尝试一下实际生活的味道。譬如“中央苏区”的土地革命已经有三四年，农民的私人日常生活究竟有了怎样的具体变化？他们究竟是怎样的感觉？我曾经去考察过一两次。一开口就没有“共同的语言”，而且自己也懒惰得很，所以终于一无所得。<br />
<br />
　　可是，自然而然地，我学着比较精细地考察人物，领会一切“现象”。我近年来重新来读一些中国和西欧的文学名著，觉得有些新的印象。你从这些著作中间，可以相当亲切地了解人生和社会，了解各种不同的个性，而不是笼统的“好人”、“坏人”、或是“官僚”、“平民”、“工人”、“富农”等等。摆在你面前的是有血有肉有个性的人，虽则这些人都在一定的生产关系、一定的阶级之中。<br />
<br />
　　我想，这也许是从“文人”进到真正了解文艺的初步了。<br />
<br />
　　是不是太迟了呢？太迟了！<br />
<br />
　　徒然抱着对文艺的爱好和怀念，起先是自己的头脑，和身体被“外物”所占领了。后来是非常的疲乏笼罩了我三四年，始终没有在文艺方面认真地用力。书是乱七八糟地看了一些；我相信，也许走进了现代文艺的水平线以上的境界，不致于辨别不出兴趣的高低。我曾经发表的一些文艺方面的意见，都驳杂得很，也是一知半解的。<br />
<br />
　　时候过得很快。一切都荒疏了。眼高手低是必然的结果。自己写的东西――类似于文艺的东西是不能使自己满意的，我至多不过是个“读者”。<br />
<br />
　　讲到我仅有的一点具体知识，那就只有俄国文罢。假使能够仔细而郑重地，极忠实地翻译几部俄国文学名著，在汉字方面每字每句地斟酌着，也许不会“误人子弟”的。这一个最愉快的梦想，也比创作和评论方面再来开始求得什么成就，要实际得多。可惜，恐怕现在这个可能已经“过时”了！<br />
<br />
告别<br />
<br />
　　一出滑稽剧就此闭幕了！<br />
<br />
　　我家乡有句俗话，叫做“捉住了老鸦在树上做窝”。这窝始终是做不成的。一个平心甚至无聊的“文人”，却要他担负几年的“政治领袖”的职务。这虽然可笑，却是事实。这期间，一切好事都不是由于他的功劳――实在是由于当时几位负责同志的实际工作，他的空谈不过是表面的点缀，甚至早就埋伏了后来的祸害。这历史的功罪，现在到了最终结算的时候了。<br />
<br />
　　你们去算帐罢，你们在斗争中勇猛精进着，我可以羡慕你们，祝贺你们，但是已经不能够跟随你们了。我不觉得可惜，同样，我也不觉得后悔，虽然我枉费了一生心力在我所不感兴味的政治上。过去的是已经过去了，懊悔徒然增加现在的烦恼。应当清洗出队伍的，终究应当清洗出来，而且愈快愈好，更用不着可惜。<br />
<br />
　　我已经退出了无产阶级的革命先锋队伍，已经停止了政治斗争，放下了武器。假使你们――共产党的同志们――能够早些听到我这里写的一切，那我想早就应当开除我的党籍。象我这样脆弱的人物，敷衍、清极、怠惰的分子，尤其重要的是空洞地承认自己错误而根本不能够转变自己的阶级意识和情绪，而且，因为“历史的偶然”，这并不是一个普通党员，而是曾经当过政治委员的――这样的人，如何不要开除呢？<br />
<br />
　　现在，我已经是国民党的俘虏，再来说起这些，似乎是多余的了。但是，其实不是一样吗？我自由不自由，同样是不能够继续斗争了。虽然我现在才快要结束我的生命，可是我早已结束了我的政治生活。严格地讲，不论我自由不自由，你们早就有权利认为我也是叛徒的一种。如果不幸而我没有机会告诉你们我的最坦白最真实的态度而骤然死了，那你们也许还把我当一个共产主义的烈士。记得一九三二年讹传我死的时候，有的地方替我开了追悼会，当然还念起我的 “好处”。我到苏区听到这个消息，真我不寒而栗，以叛徒而冒充烈士，实在太那个了。因此，虽然我现在已经囚在监狱里，虽然我现在很容易装腔作势慷慨激昂而死，可是我不敢这样做。历史是不能够，也不应当欺骗的。我骗着我一个人的身后虚名不要紧，叫革命同志误认叛徒为烈士却是大大不应该的。所以虽反正是一死，同样是结束我的生命，而我决不愿意冒充烈士而死。<br />
<br />
　　永别了，亲爱的同志们！――这是我最后叫你们“同志”的一次。我是不配再叫你们“同志”的了。告诉你们：我实质上离开了你们的队伍好久了。<br />
<br />
　　唉！历史的误会叫我这“文人”勉强在革命的政治舞台上混了好些年。我的脱离队伍，不简单地因为我要结束我的革命，结束这一出滑稽剧，也不简单地因为我的痼疾和衰惫，而是因为我始终不能够克服自己绅士意识，我究竟不能成为无产阶级的战士。<br />
<br />
　　永别了，亲爱的朋友们！七八年来，我早已感觉到万分的厌倦。这种疲乏的感觉，有时候，例如一九三○年初或是一九三四年八、九月间，简直厉害到无可形容、无可忍受的地步。我当时觉着，不管全宇宙的毁灭不毁灭，不管革命还是反革命等等，我只要休息，休息，休息！！好了，现在已经有了“永久休息”的机会。<br />
<br />
　　我留下这几页给你们――我最后的最坦白的老实话。永别了！判断一切的，当然是你们，而不是我。我只要休息。<br />
<br />
　　一生没有什么朋友，亲爱的人是很少的几个。而且除开我的之华以外，我对你们也始终不是完全坦白的。就是对于之华，我也只露过一点口风。我始终带着假面具。我早已说过：揭穿假面具是最痛快的事情，不但对于动手去揭穿别人的痛快，就是对于被揭穿的也很痛快，尤其是自己能够揭穿。现在我丢掉了最后一层假面具。你们应当祝贺我。我去休息了，永久去休息了，你们更应当祝贺我。<br />
<br />
　　我时常说，感觉到十年二十年没有睡觉似的疲劳，现在可以得到永久的“伟大的”可爱的睡眠了。<br />
<br />
　　从我的一生，也许可以得到一个教训：要磨练自己，要有非常巨大的毅力，去克服一切种种“异己的”意识以至最微细的“异己的”情感，然后才能从“异己 的”阶级里完全跳出来，而在无产阶级的革命队伍里站稳自己的脚步。否则，不免是“捉住了老鸦在树上做窝”，不免是一出滑稽剧。<br />
<br />
　　我这滑稽剧是要闭幕了。<br />
<br />
　　我留恋什么？我最亲爱的人，我曾经依傍着她度过了这十年的生命。是的，我不能没有依傍。不但在政治生活里，我其实从没有做过一切斗争的先锋，每次总要先找着某种依傍。不但如此，就是在私生活里，我也没有“生存竞争”的勇气，我不会组织自己的生活，我不会做极简单极平常的琐事。我一直是依傍着我得十分难受，因为我许多次对不起我这个亲人，尤其是我的精神上的懦怯，使我对于她也终究没有彻底的坦白，但愿她从此厌恶我，忘记我，使我心安罢。<br />
<br />
　　我还留恋什么？这美丽的世界的欣欣向荣的儿童，“我的”女儿，以及一切幸福的孩子们。<br />
<br />
　　我替他们祝福。<br />
<br />
　　这世界对于我仍然是非常美丽的。一切新的、斗争的、勇敢的都在前进。那么好的花朵、果子、那么清秀的山和水，那么雄伟的工厂和烟囱，月亮的光似乎也比从前更光明了。<br />
<br />
　　但是，永别了，美丽的世界！<br />
<br />
　　一生的精力已经用尽，剩下一个躯壳。<br />
<br />
　　如果我还有可能支配我的躯壳，我愿意把它给医学校的解剖室。听说中国的医学校和医院的实习室很缺乏这种实验用具。而且我是多年的肺结核者(从一九一九年到现在)，时好时坏，也曾经照过几次Ｘ光的照片。一九三一年春的那一次，我看见我的肺部有许多瘢痕，可是医生也说不出精确的判断。假定先照过一张，然后把这躯壳解剖开来，对着照片研究肺部状态，那一定可以发见一些什么。这对肺结核的诊断也许有些帮助。虽然我对医学是完全外行，这话说的或许是很可笑的。<br />
<br />
　　总之，滑稽剧始终是完全落幕了。舞台上空空洞洞的。有什么留恋也是枉然的了。好在得到的是“伟大的”休息。至于躯壳，也许不能由我自己作主了。<br />
<br />
　　告别了，这世界的一切！<br />
<br />
　　最后……<br />
<br />
　　俄国高尔基的《四十年》、《克里摩・萨摩京的生活》，屠格涅夫的《罗亭》，托尔斯泰的《安娜・卡里宁娜》，中国鲁迅的《阿Ｑ正传》，茅盾的《动摇》，曹雪芹的《红楼梦》，都很可以再读一读。<br />
<br />
　　中国的豆腐也是很好吃的东西，世界第一。<br />
<br />
　　永别了！<br />
<br />
　　（一九三五・五・二三）</div>

							
							
							
							
