<br />
2005年中国地震局局长陈建民的公开谈话，至今言犹在耳：<br />
<br />
　　海城地震预报成功确实是人类历史上一次非常典型的地震预报成功，也是第一次，所以我们老管它叫里程碑。<br />
<br />
<br />
<br />
<br />
<br />
　　有第一次，自然会有第二次。<br />
<br />
<br />
<br />
　　西安地图出版社正式出版、中国地震局兰州地震研究所研究员郭安宁的专著《5.12汶川地震预报回顾》，正文332页，图文并茂，定价28元<br />
<br />
郭安宁&nbsp; &nbsp;研究员（教授），硕士研究生导师，毕业于成都理工大学，中国地震局兰州地震研究所、甘肃省地震局工作，甘肃省第三届青年科技奖荣誉称号获得者。<br />
<br />
　　那就只列出部分提纲，要知详情请与作者联系。<br />
<br />
　　第三章&nbsp;&nbsp;地方地震系统、个人、学术研究－前兆异常观测及预测意见<br />
<br />
　　一、地方地震系统在震前召开的雅安会议上呈现的异常及预测意见<br />
<br />
　　（一）在地方地震局中作出最好预测结论的凉山州防震减灾局<br />
<br />
　　（二）上报了异常的甘孜州防震减灾局<br />
<br />
　　（三）有异常上报的雅安市防震减灾局<br />
<br />
　　（四）一般化的都江堰市防震减灾局<br />
<br />
　　（五）有中震估计上报异常的阿坝州防震减灾局<br />
<br />
　　（六）报了异常的乐山市防震减灾局<br />
<br />
<br />
　　<br />
封底：<br />
<br />
在黑色背景白色字体写着――<br />
<br />
谨此向在汶川特大地震中遇难的同胞表示深深的哀悼！ <br />
<br />
<br />
２００８年１２月２３日，中国地震局西楼３２６会议室，中国地震预测咨询委员会召开汶川地震反思会议。本人作为网友代表列席了会议。<br />
　　针对中国地震局所谓“地震不可预测”、“汶川地震前没有前兆”的论调，众多地震专家作证指出：<br />
　　一、汶川大震前关于形势背景分析与趋势预测的对立意见<br />
　　１、２００２年，四川省地震局工程师李有才得知离映秀仅２０公里处将建设紫坪铺水库后，多次给国务院、四川省政府、中国地震局与水利部门写信，呼吁此地为强震易发地区，有７.５级大地震背景。中国地震局与四川省地震局表示对李多次上书行为强烈不满，指责李“态度不慎重，工作不严谨，给领导添麻烦”。<br />
　　２、２００６年９月，中国地震预测咨询委员会主任郭增建预测下一次强震将在康定――天水一带发生，并将文字意见亲自交与地震局岳明生副局长（中国地震局的地震预报职能部门“分析预报室”已撤消）。２００７年７月，郭增建再次强调康定地区是应注意的重点危险区。<br />
　　３、２００７年７月，四川省地震局高级工程师周荣军等在《构造学》发表《西藏高原东部边缘北川与彭灌断层的活动板块运动》指出：地壳撞击的能量在四川汶川县积累，并将以地震形式释放出来，北川附近某些断层有可能导致严重地震灾害的潜在风险。<br />
　　４、２００７年１０月２８日，四川省地震局地震地质勘察中心高级工程师龙德雄正式预测：２００８年１１月前在四川北部有发生７级以上地震的可能性。<br />
　　但是，２００７年１２月中国地震局对２００８年度全国地震重点危险区汇总研究的意见是：中国大陆最高发生６―７级地震，一般为小于等于６级危险，并提出５个重点危险区，汶川不在其列！同月，原四川省地震局局长韩渭英在《四川地震》发表文章指出：认为近期川、滇地区可能发生７级强震的预测是错误的，其观点已形成四川省地震局文件。<br />
　　二、汶川大震前关于近期地震预测的对立意见<br />
　　１、２００８年３月２１日，四川省地震局退休工程师李有才给中央领导、四川省政府、中国地震局写信预测：紫坪铺水库地区已形成４级地震围空，表明可能发生７级以上的大震趋于明显，形势已十分紧急，要求立即启动应急预案。<br />
　　２、２００８年４月１５日，四川德阳市地震局工程师潘正权向四川省地震局反映，３月下旬什邡市发现一些水井变黑的宏观异常，在没有得到省地震局重视的情况下，他不得已以什邡市防震减灾局正式文件的形式（什邡市震［２００８］６号文件），发布此宏观异常肯定为地震前兆。<br />
　　３、２００８年４月２６日，中国地震预测咨询委员会委员耿庆国在多次强调川、甘、青交界阿坝州等地区将可能发生７.５级强震的背景下，明确提出５月８日前后１０天为可能发震时间。<br />
　　４、２００８年５月３日，中国地震预测咨询委员会委员、北京工业大学教授李均之记录到次声波３３００mv特大异常，表明近期将有强震发生。<br />
　　５、２００８年５月１０日，中国地震预测咨询委员会委员钱复业、赵玉林发现ＨＲＴ波出现突出异常。５月１１日晚已初步认定此异常反映一次７.８――８.０级地震即将发生，震中距冕宁观测台约６００公里，发震时间已十分迫近。<br />
　　从中国地震局与四川省地震局的正式报告看，汶川大震前犯了严重的战略性判断的错误，随着汶川大震的逼近，对震情估计的调子越来越低，地震强度从７降为６，再从６降为５，临震前甚至认为无５级地震的可能。<br />
<br />
　　２００８年５月１２日14时28分04秒，中国四川省汶川发生里氏８.０级强烈地震，伤亡、失踪４６００００余人，直接经济损失8451亿元人民币 。<br />
<br />
“从中国地震局与四川省地震局的正式报告看，汶川大震前犯了严重的战略性判断的错误，随着汶川大震的逼近，对震情估计的调子越来越低，地震强度从７降为６，再从６降为５，临震前甚至认为无５级地震的可能。”<br />
