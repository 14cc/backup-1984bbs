1975年河南板桥水库溃坝事件 <br />
时间：2005/05/31 出处： <br />
王维洛 <br />
<br />
　　１９９４年，水利部长江流域委员会主任，国务院长江三峡建设委员会副主任魏廷铮（相当于部长级）在马来西亚沙捞越州关于当地巴贡水库的演讨会上被问及中国河南１９７５年水库溃坝事件时说，具体死亡人数不记得，但是不会超过一万人。他的理由是，如果水库溃坝事件的死亡人数超过万人，国际新闻界必然会有报导。 <br />
<br />
　　魏廷铮说的是谎话！ <br />
<br />
　　第一，１９７５年河南板桥水库和石漫滩水库两座大型水库及竹沟、田岗等数五十八座中小型水库溃坝的死亡人数远远超过一万人；第二，当时中国封锁了这个消息，世界新闻界当然无法知道。 <br />
<br />
　　１９７５年八月，河南省南部驻马店地区出现暴雨，板桥水库和石漫滩水库两座大型水库及竹沟、田岗等数五十八座中小型水库几乎同时溃坝，遂平、西平、汝南、平兴、新蔡、漯河，临泉七个县城被水淹数米深，共２９个县市受灾，涉及一千二百万人，毁房６８０余万间，冲毁京广线铁路一百多公里，京广线中断１８天，影响正常通车４８天，直接经济损失约为１００亿。板桥水库和石漫滩水库等水库溃坝事故，在当时未作公开报导・查找当年的报纸，只能发现几段关于河南省军民奋勇抵抗洪水灾害，和国务院副总理纪登奎慰问河南省军民的报导，这说明河南省南部驻马店地区那时曾发生过洪水・在大陆出版的气象学和水利学的书籍中，有提及这次事故的，但是关于死亡人数各种说法之间出入很大・至今为至，中国政府还没有公开发表过全面的调查报告和系统的事故分析。据说水利部淮河委员会在溃坝事故发生四年后，也就是在１９７９年曾作过一个板桥水库和石漫滩水库等水库溃坝事故调查报告，没有公开发表，而仍然被当作保密文件锁在保险柜中。另外，曾拍摄过一部关于１９７５年八月河南省暴雨的科学教育片，其中有关于板桥水库等水库溃坝事件，但是该片也是内部发行，只供气象和水利的专业人员观看・笔者曾在大学学习期间，作为气象学的课程内容在１９７９年看过这部影片。 <br />
<br />
　　１９８２年邓小平批准三峡工程上马，遭到国内外许多人士的反对，水库大坝的安全也是一个重要因素，这样板桥水库等水库溃坝事件也浮出水面。特别是全国政协委员乔培新、孙越崎、林华、千家驹、王兴让、雷天觉、徐驰和陆钦侃在文章中揭露，死亡人数达２３万人，引起国人注意。但是水利部说板桥水库溃坝事件的死亡人数是２・６万。以后国内有文章提及这一事件。中国环保作家郑义也曾作过调查，发表过有关文章。１９９２年，板桥水库大坝重建，并立碑，由水利部长钱正英题写碑文。１９９５年２月，亚洲人权观察发表了关于板桥水库和石漫滩水库失事的报导，引起世人震惊。 <br />
<br />
　　二、事故死亡人数 <br />
<br />
　　有多少人死于这次水库溃坝灾难，有各种不同的说法，有代表的有如下一些： <br />
<br />
　　全国政协委员和政协常委乔培新、孙越崎、林华、千家驹、王兴让、雷天觉、徐驰和陆钦侃揭露，死亡人数达２３万人；孟昭华和彭传荣编的中国灾荒史中载录，板桥水库和石漫滩水库垮坝失事，１０２９万人遭受毁灭性的水灾，约有十万人当即被洪水卷走；中国科学院大气物理研究所的研究员蔡则怡和赵思雄说，死亡近十万人；中国科学院著名的气象学家陶诗言写到，死亡人数达数万人；板桥水库重新修建碑文中刻有，卷走数以万计人民的生命财产；水利部减灾研究中心研究员刘树坤和程晓陶没有直接提供死亡人数，只指出人员伤亡惨重；中国科学院环境科学委员会副主任郭方没有直接提供死亡人数，他写到，１９７５年８月河南大洪水冲垮了两座水库，下游造成了灾难性的后果・姜国亭说，近三万人死于非命（其中板桥水库失事占总归数的８０％）・ <br />
<br />
<br />
　　上海辞书出版社的水利词典记载，板桥水库和石漫滩水库垮坝，死亡二万六千人・在由水利部组织和委托编写的中国水灾史一书中有一章节是关于１９７５年淮河洪水的，书中写道，１９７５年水库溃坝事件的死亡人数为２・６万人・但是同在这本书的前言中（执笔者：南京水文研究所骆承政）写道，１９７５年水库溃坝事件的死亡人数为８５６００・而这８５６００的数字又被淮河洪水一章在脚注种作为错误被指出・这就使人难以理解，为什么在同一本书内，关于同一个事故会有两个不同的数据，而其中一个又被作为错误被指出，而在编辑时却没有得到修正・三峡工程建设委员会付主任魏廷铮说，事故死亡人数不可能超过万人。 <br />
<br />
<br />
　　要搞清魏廷铮说的是真话还是谎话，有必要先对这个事件做一简单介绍。 <br />
<br />
<br />
　　三、建造水库是毛泽东根治淮河的最主要措施 <br />
<br />
<br />
　　板桥水库和石漫滩水库位于淮河的支流上，是所谓治理淮河的最重要工程的一部份・１９５０年中共中央治淮委员会成立，１９５１年毛泽东题词：“一定要把淮河修好・”之后，淮河流域上的一大批水库工程开始兴建，其中大部份工程在１９５６年和１９５７年又经扩建和加固・板桥水库和石漫滩水库就是这个年代的产物・至五十年代末，在淮河上游建造了佛子岭，梅山，磨子潭、响洪甸、南湾、薄山、白沙、板桥和石漫滩共九座大型水库和无数的中小型水库，其目的就是蓄水・当时把这个水利策略叫作“满天星”和“葡萄串”・至此，治理淮河的水库技术措施已经是全部实施了，再找不到适合建水库的坝址了・当时人们遗憾的只是在淮河流域上找不到一个象三峡水库一样的，能称雄世界的工程，一个能把淮河洪水卡住的工程，一个能一劳永逸的工程・ <br />
<br />
<br />
　　四、溃坝原因 <br />
<br />
<br />
　　１９７５年７月底，第三号台风在美国关岛附近的洋面上形成，台风形成后向西北方向移动，８月３日台风穿过台湾中部，８月４日上午２时在中国福建省的龙岩登陆，然后经江西、湖南、湖北、河南，最后返回湖北消失・８月５日台风雨区中心移到河南省南部，一日最大雨量量为６７２毫米，８月６日暴雨强度减弱，一日最大雨量仍有５１４毫米，８月７日暴雨强度增加，一日最大雨量达到１００５毫米・无论是一小时的暴雨量，还是三小时的暴雨量，无论是六小时的暴雨量，还是十二小时的暴雨量，无论是一天的暴雨量，还是三天的暴雨量，这次暴雨都创造了大陆气象站的最高记录・ <br />
<br />
<br />
　　当时最大的两个暴雨中心，正好位于淮河上游的板桥水库和石漫滩水库的上游，三天的降雨量超过１６００毫米（当地年平均降水量为８００毫米）・就是说，三天的降雨量比平均两年时间内降的雨水还多・暴雨发生之前的几个月中，河南南部降雨很少，正出现旱情，农田缺水，大部分水库蓄水位很低，不能满足灌溉和供水的需求・８月４日该地区受台风影响开始降雨，人们还以为是天上降下了及时雨，各地水库纷纷开始蓄水，抬高水位，用于抗旱・这个蓄水过程持续到８月７日・由于降雨量大，水库又只蓄不放，水位上升很快・水位上升到水库正常蓄水位，继续上升到最高蓄水位，超过警戒水位・最后板桥水库和石漫滩水库和竹沟、田岗等几十座中小型水库发生漫顶溃坝・ <br />
<br />
<br />
　　水库溃坝的时间发生在８月８日凌晨一时左右，当时人们还在梦乡之中・溃坝的洪水冲溃了河堤，将下游的村庄和房屋一扫而光，总计毁房６８０余万间・位于水库下游，与洪水流向垂直的中国最重要的铁路干线京广线也被冲毁一百多公里，一些区段铁路线被冲得荡然无存，一些区段的铁路线成了麻花形・京广线中断１８天・最后不得不采取爆破开口分洪的手段，分别在淮河中下游多处采用爆破的手段，炸毁堤坝以分洪，以增加受灾面积来减小溃坝洪水的毁坏力量。由于时间紧迫，许多居民没有事先得到消息，根本来不及逃离，死于爆破分洪的洪水・这次灾害涉及２０多个县市，一千二百万人，直接经济损失约为１００亿・ <br />
<br />
<br />
　　造成水库溃坝的原因有许多，有大气候的环境条件，有小气候的环境条件，有水库所在地区的地形条件，有水库设计中的错误等等，而最直接的原因是，是水库泄洪道的闸门锈死，不能被开启而造成失事，是一个由人为错误所造成灾难・ <br />
<br />
<br />
　　水库大坝是拦截水流的水工构物，在大坝后壅高水位，形成水库，用于调节水流，用于发电灌溉供水等・这种水利枢纽工程与人一样，不能只蓄不放・人要是只吃喝不拉撒，就会死亡・因此水利枢纽工程还包括泄水构筑物，用于渲泄洪水，以防洪水漫顶，避免水库大坝溃坝・而溢洪道是土石大坝的最主要的泄水构筑物・平时溢洪道的闸门是关闭的，只是发生洪水时，才将闸门打开，让洪水从溢洪道中流到下游，这样水库中的水位不会再升高，或是升高速度减缓，或是水位下降・这就取决入库的洪水流量和经溢洪道的排放流量的关系・入库的洪水流量大于溢洪道的排放流量，水库中的水位上升，入库的洪水流量越大，而溢洪道的排放流量越小，水库中的水位上升速度越快・反之，溢洪道的排放流量大于入库的洪水流量，水库中的水位下降・当溢洪道的排放流量等于入库的洪水流量时，水库中的水位不变・ <br />
<br />
<br />
　　从某种意义上来说，水库是不能用来防洪的・当溢洪道的排放流量大于或等于入库的洪水流量时，水库的防洪效为零，有时还起到雪上加霜的作用・在工程上，对溢洪道的排放能力的设计根据不同的水库有不同的要求，要求在发生历史最大洪水时，溢洪道的排放能力能保障水库大坝不溃坝・由于８月４日前有旱情，水库水位低，水库的可蓄水的库容大・当时为了蓄水，溢洪道的闸门都是紧闭的・也没有人去查看过闸门・其实，泄洪道的闸门自五十年代后期水库工程扩建以来，就没有用过，也没有人去检查过・ <br />
<br />
<br />
　　由于暴雨大，入库的水流量也大，泄洪道的闸门没打开，泄洪道的排放流量为零，因而水库的水位上升很快・当八月七日，特大暴雨降临后，板桥水库水位超过了警戒水位时，这时才下令去打开水库的泄洪道闸门排放库水・可是谁也没有想到，在这最紧急的关头，泄洪道的闸门确打不开・泄洪道的闸门因为多年没有开启早就被锈死了，闸门打不开，泄洪道也起不到泄洪的作用・这时已经没有时间再设法把闸门打开，或是用炸药炸毁泄洪道的闸门，以保大坝安全・一切都为时太晚・洪水冲溃了大坝，致使下游十余座水库相续溃坝，附近的城镇遭受灭顶之灾・ <br />
<br />
<br />
　　板桥水库的总库容量有５亿多立方米，而１９７５年八月三天的进入水库的水量有６・９７亿立方米，由于泄洪道未被打开，进入水库的水量大于水库的尚存的库容量，溢顶溃坝是在劫难怪逃・板桥水库的溃坝效用和破坏能力，大大超出了人们的预料和人们的想象・板桥水库大坝高仅为２５米，库容仅为５亿立方米，板桥水库最大溃坝流量达到７８２００立方米／秒・这流量有多大？ <br />
<br />
<br />
　　这个最大溃坝流量比１９５４年长江洪水宜昌站的洪峰流量６６８００立方米／秒更大，比１９８１年长江洪水宜昌站的洪峰流量７２０００立方米／秒还大，这个流量超过了宜昌站实际测到的历史最大洪水量（不是指根据洪水痕迹推算的历史最大洪水量）・而板桥水库所在的淮河支流汝河，与浩浩荡荡的长江相比，是微不足道的・当板桥水库的溃坝洪水冲到下游约四十公里处的遂平县城时，还有５３４００立方米／秒的洪峰流量，这洪水量与长江干流宜昌站的十年一遇的洪水流量相妨・溃坝洪水波以立浪或涌波形式向下游急速推进，时速在３０到５０公里，与汽车速度相妨，下游附近地区的居民没有时间加以防范和逃避・加上正好是在午夜，人们都在梦乡之中・溃坝洪水的波峰在传播初期很高，立波经过处的河槽水位瞬息剧涨，水流汹涌湍急，破坏力特别强。 <br />
<br />
<br />
　　五、定义问题 <br />
<br />
<br />
　　１９７５年８月板桥水库和石漫滩水库两座大型水库及竹沟、田岗等五十八座中小型水库发生溃坝，这一事件在国际上被称统称为“板桥水库溃坝事件”，它不但包括板桥水库，还包括石漫滩水库、竹沟、田岗等其他水库。这一事故的总死亡人数，包括直接死于溃坝洪水的居民，也包括死于炸堤分洪洪水的百姓，即死于溃坝洪水后续影响的人。 <br />
<br />
<br />
　　这就是全国政协委员和政协常委乔培新、孙越崎、林华、千家驹、王兴让、雷天觉、徐驰和陆钦侃揭露，死亡人数达２３万人。这和中国科学院环境科学委员会副主任郭方说的给下游造成了灾难性的后果，是一致的。因为板桥水库溃坝事件的危害，应该包括所有的灾难性的后果，这些灾难集中在水库的下游。 <br />
<br />
<br />
　　孟昭华和彭传荣编说，约有十万人当即被洪水卷走；中国科学院大气物理研究所的研究员蔡则怡和赵思雄说，死亡近十万人和中国科学院著名的气象学家陶诗言写到，死亡人数达数万人，是指直接死于溃坝洪水的居民人数，未包括死于后继影响的人，如炸堤分洪等措施所造成死亡人数。 <br />
<br />
<br />
　　而直接死于板桥这一座水库大坝溃坝的人，为一到十万人之间。钱正英在写碑文的时候用的是“卷走数以万计人民的生命财产”，是指仅板桥大坝溃坝所造成的死亡人数，而没有包括石漫滩水库及竹沟、田岗等其他水库溃坝所造成的死亡人数，更没有包括炸堤分洪所造成的死亡人数。 <br />
<br />
<br />
　　骆承政写道，１９７５年水库溃坝事件的死亡人数为８５６００，是指板桥水库和石漫滩等水库溃坝的死亡人数，而水利部所说的２・６万是直接死于板桥水库溃坝洪水的人。就是板桥一座水库溃坝所造成的死亡人数，也远远超过一万人，可见魏廷铮在说谎。 <br />
<br />
<br />
　　由于国际上所指的“板桥水库溃坝事件”，是指１９７５年８月河南省众多水库发生溃坝所造成人民生命财产损失的大事故，死亡人数不仅应该包括直接被溃坝洪水卷走的人，也应该包括炸堤分洪所造成的死亡人数，这个死亡人数就是陆钦侃等所揭露的２３万人。 <br />
<br />
<br />
　　希望水利部能公布板桥水库溃坝事件的调查报告，并公布在这一事件中的死亡总人数，而不是仅仅公布死于板桥这一座水库大坝溃坝洪水的人数。 <br />
<br />
<br />
　　最后可以对比一下板桥溃坝洪水与淮河自然洪水所造成的灾害程度・１９４９年夏，淮河流域发生水灾，河堤多处决口，受灾面积２５００万亩・１９５０年夏，淮河流域再次发生百年不遇的严重水灾，２７个县受灾，受灾人口９９０万，受灾面积３１００万亩，被冲毁和破坏的房屋达８０余万间，死亡人数４８９人・正是１９５０年的水灾促使了修筑板桥等水库的决策，来治理淮河・二十五年后，水库溃坝，造成２３万人死亡，损失孰大孰小，不言自明。 <br />
<br />
<br />
　　板桥等水库溃坝发生在午夜，许多人在梦中就被洪水卷走了，许多家庭，许多村落是无一人生还。在这里，让我们燃一柱香，烧一张纸，祈祷那些不知为何而死的死者能安息于九泉之下。让我们燃一柱香，烧一张纸，保佑中国大地上的八万多座大坝不要再发生这样的悲剧，特别保佑魏廷铮参与领导、论证、规划、设计、建造的长江三峡大坝不要再发生这样的悲剧！ <br />
