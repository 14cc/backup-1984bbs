10 top technological catastrophe in the world No.1<br />
2005年5月28日看了discovery中“10 top technological catastrophe in the world”节目，才知道，在世界历史上“人为技术错误造成的灾害”的名次，居于印度博怕尔化工厂泄毒事件和前苏联切尔诺贝利核电站爆炸事件之上，而列第一的，是在我们中国：1975年河南板桥水库垮坝，打捞到的尸体10万多具，后续因缺粮、感染、传染引起的死亡14万，共24万多人死亡，与次年的最大自然灾害唐山大地震死人数相仿，比埃及阿斯旺水库垮坝还更祸害人。 <br />
<br />
<br />
<br />
而我们国人从不知情。 <br />
<br />
几天之内，全地区有大小26座水库相继崩堤垮坝，9县1镇东西150公里，南北75公里范围内一片汪洋。400多万群众被洪水围困，10多万群众死亡，30多万头大牲畜漂没，300多万间房屋倒塌，直接经济损失34・97亿元，相当于建专区以来十几年财政收入的总和。 <br />
<br />
<br />
<br />
板桥水库大坝，位于多灾贫困的河南驻马店地区，三门峡水库大坝之西南，是大跃进的产物，工程质量粗劣，又无正常维护，至灾害发生时，17个泄洪闸只有五座能开启。1975年8月初，一场台风引发了当地历史上千年一遇的特大暴雨。河南泌阳县境内汝河上游的板桥水库水位暴涨，水库管理人员在没有得到上级命令的情况下，不敢大量排水泄洪，而外地区石漫滩水库的大量洪水急骤流入板桥水库，加快了板桥水库水位暴涨的速度。 <br />
<br />
<br />
<br />
8月7日19时30分，驻守在板桥水库的34450部队军内的通讯设备向上级部门发出特特急电称：“板桥水库水位急遽上升，情况十分危急，水面离坝顶只有1・3米，再下300毫米雨量水库就有垮坝危险！”仅仅7个小时后，8日零时20分，水库管理局第二次向上级部门发出特特急电，请求用飞机炸掉副溢洪道，确保大坝安全。 <br />
<br />
<br />
<br />
可是，同第一封急电一样，这封电报同样没能传到上级部门领导手中。40分钟后，高涨的洪水漫坝而过。水库管理局第三次向上级部门发出特特告急电，并开启尚能移动的五扇闸门，此时水库已经开始决口。4时，当地驻军冒着被雷劈电击的危险，将步话机天线移上房顶，直接在房顶上与上级有关部门取得联系，报告了板桥水库险情。同时，为及时报告水库险情，让下游群众紧急转移，在无法与外界沟通的危急情况下，驻军曾几次向天空发射红色信号弹报警。可是，由于事先没有约定危急时刻的报警信号，下游群众看到信号弹后不知道发生了什么事情。 <br />
<br />
<br />
<br />
8日凌晨，洪水像脱缰的野马，冲出板桥水库的决口，以每秒6米的速度夺路狂奔，铺天盖地地向下游冲去。仅仅6个小时，板桥水库就向下游倾泄7・01亿立方米洪水。至遂平县境内时，水面宽10公里，水头高3─7米。昔日人欢马叫的遂平县城，俄顷之间一片汪洋。沉睡在梦乡中的人们，在浑然不觉中被埋在水底，变成水底的冤魂。 <br />
<br />
<br />
<br />
洪水铺天盖地向下游奔腾而去。所到之处，水库垮坝，堤塘决口。决口的洪水与上游来水合二为一，汇合成更大更猛的洪水一路狂奔，劈头盖脑地淹没了广大的城镇和乡村。据后来统计，整个驻马店地区96％的面积受灾，许多地方一片汪洋，平均水深3─7米，300多万人口被围困在洪水中。 <br />
<br />
<br />
<br />
此时上级才决定炸开刘埠口小洪河左堤、洪□（大洪河和分洪道之间的□地）圈堤及河上阻水堤坝分洪，同时指示要确保该地区亚洲最大的人工平原湖宿鸭湖的安全。但已经晚了。几天之内，全地区有大小26座水库相继崩堤垮坝，9县1镇东西150公里，南北75公里范围内一片汪洋。400多万群众被洪水围困，10多万群众死伤，30多万头大牲畜漂没，300多万间房屋倒塌，随后又有14万多的灾民无以生计走向黄泉。 <br />
