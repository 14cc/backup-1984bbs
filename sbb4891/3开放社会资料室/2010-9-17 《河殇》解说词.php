生活中丰富的海洋文明的因素，就像几缕细细的清泉，淌到内陆文明的黄土板块<br />
上，立刻就无影无踪了。当内陆文明中华夏大地蒸蒸日上的时候，蔚蓝色的海洋<br />
文明，正在地中海悄悄崛起了。早在古希腊时代，雅典的民主思想，正是随著雅<br />
典的海上权力一同兴起的。海权导致了民族革命。<br />
<br />
　　近代西方资产阶级革命的社会前提，也正是欧洲海外航线的开辟。从十五世<br />
纪开始航行于海天之间的那些帆船，既揭开了世界贸易和殖民活动的帷幕，同时<br />
也运载著科学和民主的希望。蔚蓝色就像这小帆船，获得了现代世界命运的象征<br />
意义。<br />
<br />
　　于是，广大的东方市场和美洲新大陆，使小小的欧洲几乎一夜之间成为暴发<br />
户。<br />
<br />
　　横渡大洋需要又坚固，又庞大，又精巧的船舶，造出这样的船舶需要数学和<br />
物理学，需要技术和科学。于是，一六三六年，迦利列发表了「新科学对话」，<br />
这场对话，就是在造船厂举行的。<br />
<br />
　　英国首先由海外贸易获得巨大利益，促进了资本的原始积累，也促进了自由<br />
思想的普及，于是首先在英国发生了克伦威尔领导的资产阶级革命。一六五一年，<br />
克伦威尔颁布航海条例。一六九零年，洛克发表「政府论」。自由贸易论成为资<br />
产阶级的口号和原则。　资本主义转动著工业革命和自由贸易这两个轮子，开始<br />
了伟大的飞跃，开始了科学与民主的双重历史大和唱。<br />
<br />
　　这一切，都与海洋息息相关。<br />
<br />
　　中国这时候在干甚么呢？<br />
<br />
　　当麦哲仑正航行在他的环球航线上时，明朝嘉靖皇帝因为日本贡使打架，开<br />
始正式「闭关」。<br />
<br />
　　公元一七七六年，亚当斯密发表了著名的「国富论」。就在这本书中，他宣<br />
布中国的历史和文明停滞了。他说：停滞是由于不重视海外贸易，闭关必趋于自<br />
杀。<br />
<br />
　　可惜，这些话没有一个中国人能够及时听到。<br />
<br />
　　终于，当虎门码头烧起那把著名的大火，揭开了耻辱的中国现代史的时候，<br />
中国同西方之间，已经隔著一条巨大的精神文化鸿沟了。一个扩张的，进行国际<br />
贸易和战争的蔚蓝色文明，同一个坚持农业经济和官僚政治的黄色文明间的文化<br />
对抗，无疑是冰炭不相容的。<br />
<br />
　　然而只要一交手，西方的坚船利炮马上就让中国的官员和士大夫们领教了蔚<br />
蓝色的强大。于是，有了「洋务运动」，有了「中体西用」。<br />
<br />
　　洋务大臣们买回来了威力强大的铁甲军舰，办起了一座座兵工厂。位于上海<br />
市郊的江南兵工厂，在掌握西方技术能力方面远远超出日本。一八七零年左右，<br />
俄国人参观远在中国西北的兰州兵工厂时，为那里制造的枪枝质量之精良而感到<br />
震惊。甲午战争开始时，中国的军舰数量比日本要多。<br />
<br />
　　然而，这一切并没有阻止清王朝先败于法国，再败于日本。甲午战争失败的<br />
最直接原因，竟是由于一个腐败的承包商在许多炮弹里灌了泥沙。成半月形展开<br />
的中国舰队，临战了还不知到底应该听从谁的指挥。这一事实清楚地说明了，腐<br />
败的制度说导致的必然失败，并不能靠技术来挽回。<br />
<br />
　　清朝政府最早派送到英国学习海军的留学生严复，日后并没有去当一名战舰<br />
指挥官，而是成了思想启蒙家。<br />
<br />
　　严复从对西方的大量观察中发现，欧洲文化的伟大功绩，就在于发挥个人的<br />
潜力，提供一种社会契约，这种契约能使竞争以及资本主义的其他一切功能，都<br />
有利于促进社会改革。同样，通过利用个人的意志力――一种人类的「浮士德」<br />
和「普罗米修斯」式的能力，就能创造出一种生机勃勃的文化。<br />
<br />
　　然而，当严复参与其事的百日维新惨败之后，日本的明治唯新却成功了。当<br />
这位位中国近代的伟大启蒙者在封建势力的打击下，一步步放弃改良思想，最终<br />
倒退到孔孟之道的怀抱里去的时候，他在英国海军大学的同学伊藤博文，却连任<br />
日本首相，率领这个岛国迅速跨进世界强国之林。<br />
<br />
　　严复乃至近代许多伟大思想先躯如康有为，梁启超，章太炎等等的悲剧命运，<br />
似乎都证明了，即使是最优秀的中国人在革命和激进了一阵之后，到头来都摆脱<br />
不了退回儒家的归宿。　直到八十年代的今天，在中国文化热的大讨论中，人们<br />
依然在继续著中西文化优劣的百年未决的争议。无论是全盘西化的一派幻想，还<br />
是儒家文明第三繁荣期的一厢情愿，一切都仿佛还在原地踏步。难怪有的青年学<br />
者这样感叹道：巨大的文化财产变成了巨大的文化包袱，巨大的文化优越感变成<br />
了巨大的文化负罪感，这不能不说是中国现代化进程中一个巨大的心理障碍。<br />
<br />
　　变法之艰难，或许就难在我们总是担心中国人还是中国人吗？我们似乎并不<br />
知道，在以往西方的二三百年里，不论是文艺复兴，宗教改革，还是启蒙运动，<br />
西欧人至少从未担心过，在改革之后是否会变成不是意大利人了，不是德意志人，<br />
不是法兰西人了。唯独在中国，这是最大的忌讳。这或许正是那黄色文明的沉重<br />
之处和浅薄之处。<br />
<br />
　　两千多年前的哲学家庄子，给我们讲过一个寓言：黄河之神河伯，在秋天涨<br />
大水的时候，发现自己很伟大，居然两岸之间分辨不清牛马。他尽情往下游漂去，<br />
突然看见了大海，竟茫然若失。　海的主宰北海若告诉他，不能和井蛙谈论大海。<br />
因为他只知道自己那点小小的地盘，无法想象大海的博大。而想在，我的河伯，<br />
你终于走壅塞的河道，看见了大海的恢宏。你知道了局限，也就进入了一个更高<br />
的境界。这是一个象征。　它说的并不是古代中国，它好像是在预言今天。<br />
<br />
　　古老的黄河之神，真正看清大海的面貌，认识大海的博大与力量，不过一个<br />
世纪，它面对大海发出的那声长长的叹息，穿过一百多年的历史，一直回响到今<br />
天。<br />
<br />
　　这片土黄色的大地不能教给我们，甚么是真正的科学精神。<br />
<br />
　　肆虐的黄河不能教给我们，甚么是真正的民主意识。<br />
<br />
　　单靠这片黄土和这条黄河，已经养育不起日益膨胀的人口，已经孕育不了新<br />
的文化，它不再有过去的营养和精力。<br />
<br />
　　儒家文化或许有种种古老完美的法宝，但它几千年来偏偏造就不出一个民族<br />
的进取精神，一个国家的法治秩序，一种文化的更新机制；相反，它在走向衰落<br />
之中，不断摧残自己的精华，杀死自己的内部有生命的因素，窒息这个民族的一<br />
代有一代精英。<br />
<br />
　　历史证明：按照一种内陆文化的统治模式来进行现代化建设，虽然也能容纳<br />
现代科技的某些新成果，甚至卫星可以上天，原子弹可以爆炸，但却不能根本性<br />
地赋予整个民族以一种强大的文明活力。<br />
<br />
　　只有当蔚蓝色的海风终于化为雨水，重新滋润这片乾旱的黄土地时，这些只<br />
在春节喜庆日子里才迸发出来的令人惊异的活力，才有可能使巨大的黄土高原重<br />
新获得生机。<br />
<br />
　　在黄土高原的腹地――延安，到处都可以看到来自上海，浙江等沿海省市的<br />
小姑娘，小伙子们开设的服装店，理发店。　沿海地区涌来的商品散布在大小巷。<br />
神圣的，土灰色的宝塔山，在这红红绿绿，熙熙攘攘的市场后面，渐渐淡化为一<br />
个蒙胧的背景。<br />
<br />
　　这些老汉和小伙子，他们的祖先曾经从这内陆腹地出发，征服了全中国，如<br />
今却随著这片萎缩了的土地，一起萎缩了他们曾经那么旺盛的精力。令人难以置<br />
信的是，这几个小伙子，竟然是这支气势磅礴的千人腰鼓队的成员。难道他们的<br />
生命力，永远只能消耗在打腰鼓的渲泄之中吗？<br />
<br />
　　一九八零年，深圳特区建立。　它向全世界宣布，几千年的内陆文明，终于<br />
走到了大海的边上，把它面对著土地的脸，转过来远望海洋了。<br />
<br />
　　一九八六年，十四个沿海城市全面开放。中国正式摆出了向大海挑战的态势。<br />
<br />
　　一九八八年，海南省宣布成立。它的短期目标是向亚洲四小龙挑战。古老的<br />
亚洲大陆终于放下了泱泱大国的架子。<br />
<br />
　　如果海南成功，它将与十四个沿海城市连成一体，成为太平洋西岸的一条经<br />
济巨龙。这一历史壮举，比将刷新中国文化的颜色。<br />
<br />
　　但是，在改革迅速推进的时代里，有多少中国人是自觉参与其中的呢？<br />
<br />
　　中国公民政治心理调研组的系列报告指出，中国公民中较为普遍地存在著一<br />
种过于谨慎的政治参与心理：百分之六二点四一的公民表示「对谈政治问题，我<br />
很谨慎。」<br />
<br />
　　百分之七十三点七九的公民对政治上的事情还是少介入为妙的观点表示「同<br />
意」，「基本同意」，「不反对」；他们仍然担心参与政治会引起麻烦，他们仍<br />
然缺乏正直参与的安全感。　几十年正直运动的反复无常，正直迫害的极端惨烈，<br />
使人们仍然心有余悸。　这势必严重阻碍民主化的进程。　一九一九年的五四运<br />
动，第一次以彻底的不妥协精神，亮出了科学与民主的旗帜。包括马克思主义在<br />
内的西方文化思想，在中国广泛传播，但是，这种激进的文化潮流，并没有冲洗<br />
掉政治上，经济上和人格上的封建主义积淀。几十年来，时而沉渣泛起，时而一<br />
片冰封。中国的许多事情，似乎都必须从五四重新开始。<br />
<br />
　　中国历史没有给中国人造就出一个中产阶级来推动科学与民主的胜利。中国<br />
文化也没有培养出公民意识，相反，它教化出臣民心理，臣民心理只能产生逆来<br />
顺受的顺民和铤而走险的狂徒。　不过，历史却给中国人造就了一个十分独特的<br />
群体――知识分子。<br />
<br />
　　他们很难有统一的经济利益和独立的政治主张，他们几千年来都是一种附庸；<br />
他们也不可能成为一个结实的社会实体，用钢铁一般的经济实力对旧的社会进行<br />
武器的批判；他们的才华可能被利用，他们的意志可能被扭曲，他们的脊梁可能<br />
会弯曲，他们的肉体可能被消灭。<br />
<br />
　　然而，摧毁愚□和迷信的武器操在他们的手里；把科学与民主的蔚蓝色甘泉<br />
浇洒在黄土地上的是他们！<br />
<br />
　　（演播室。　学者谈当代知识分之问题。　）<br />
<br />
　　远志明（中国人民大学哲学系博士研究生）：这种现实和理想的矛盾，或者<br />
传统与理想的矛盾，首先感觉到的恐怕是中国的知识分子，尤其是近代以来的知<br />
识分子。他们往往首先发现传统的弊端，但是又没有能力去改变它。他们总是满<br />
怀希望寻求冲破传统的出路，但是他们往往有缺乏这中勇气，所以说他们总是最<br />
先感觉到心灵的痛苦。在这种情况下，我想唯一的出路就是在世界潮流的逼迫下，<br />
借助于环境的压力强迫人们去接受新生活，也忍著痛苦去创造新生活。为此，我<br />
用这样三句话加以归结：中国的希望在于世界，实现这希望要靠被世界唤醒的中<br />
国人，我们这一代注定要承受心灵的痛苦，或许能因此而变得伟大。<br />
<br />
　　比知识分子更有现实力量的，或许是这些相貌平平，谈吐也不惊人的新型企<br />
业家们。甚至在这些小商店的老板们中间，在这些急急忙忙赶路的生意人中间，<br />
在这些离开土地四处揽活的农民中间，正在积聚的新的社会能量与冲动，都不可<br />
低估。<br />
<br />
　　我们也看到，即使在以往极为森严，庄重的高级政治场合，也终于泛起了蔚<br />
蓝色的透明度。中国的大众传播，第一次处在较为中立的立场，担当了决策上层<br />
与人民群众之间相互对话的媒介。<br />
<br />
　　然而，要使专制主义传统深厚的东方人真正懂得民主是怎么一回事，并不容<br />
易。历史给我们留下了许多有趣的故事。<br />
<br />
　　一九四零年，罗斯福第三次当选为总统。蒋介石先生为此在日记中写道：<br />
「美国之民主，令人羡慕不置，特电罗斯福总统，祝其成功。」三年后的一个晚<br />
上，蒋介石的夫人也是特使宋美龄，在白宫同罗斯福夫妇共进晚餐时，谈起美国<br />
工人罢工的事情。罗斯福问宋美龄，假如中国政府在战争时期遇到这样的事情，<br />
该如何处理？宋美龄安祥地用一个涂色的长指甲在自己的脖子上划了一道，做了<br />
一个优美的杀头手势，使席间所有的美国人惊讶不止。罗斯福夫人事后说：「宋<br />
美龄对民主制度能够讲得很漂亮，但是她可不知道怎样实行民主制度。」专制政<br />
治的特点是神秘性，独裁性，随意性。<br />
<br />
　　民主政治的特点应该是透明性，民意性，科学性。<br />
<br />
　　我们正在从混浊走向透明。<br />
<br />
　　我们已经从封闭走向开放。<br />
<br />
　　黄河命定要穿过黄土高原。<br />
<br />
　　黄河最终要汇入蔚蓝色的大海。<br />
<br />
　　黄河的痛苦，黄河的希望，造就了黄河的伟大。<br />
<br />
　　黄河的伟大，也许在于它在海洋与高原之间创造了一片大陆。<br />
<br />
　　黄河来到了伟大而痛苦的入海口。<br />
<br />
　　滚滚千里泥沙，将在这里沉积为新大陆。<br />
<br />
　　汹涌澎湃的海浪，将在这里同黄河相碰撞。<br />
<br />
　　黄河必须消除它对大海的恐惧。<br />
<br />
　　黄河必须保持来自高原的百折不挠的意志与冲动。<br />
<br />
　　生命之水来自大海，流归大海。<br />
<br />
　　千年孤独之后的黄河，终于看到了蔚蓝色的大海。　（完）</div>

							
							
							
							
