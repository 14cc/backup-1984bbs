　　天子和大臣们，每年都要来这里举行亲耕。皇帝右手扶著漆金的雕龙犁，左<br />
手执鞭，在两名老者的搀扶下，在这象征土地的祭坛上步行三次，就算完成了亲<br />
耕。于是五谷丰收，指日可待。<br />
<br />
　　中国人几千年来，都是面朝黄土，背向青天，土里刨食。土地是命根子，是<br />
传家之宝，是人生的全部意义。<br />
<br />
　　几千年的文化，都凝聚在这黄土里。于是，它就显得很神秘，仿佛包裹著中<br />
国人的心魂。<br />
<br />
　　（演播室。作家谈黄河。）<br />
<br />
　　张炜（山东作家）：黄河流了好多年，它把好多秘密都渗透在两岸的泥土中。<br />
有两个老头儿，十几岁时流浪到东北去，到了七八十岁的时候，几经周折回到了<br />
自己出生的地方。这是个离黄河入海口二十多里的村庄。回去的时候，每个人从<br />
地里包了一包土走。走的前一天晚上，两个老人搂抱著，在坑上滚动著~ 哭了一<br />
夜。我一直到现在也搞不明白，这包泥土里边有什么东西？哲学家好像琢磨得更<br />
透一些。<br />
<br />
　　黑格尔曾经说过：平凡的土地，平凡的平原流域，把人类束缚在土地上，把<br />
他们卷入无穷的依赖性里边，但是大海却挟著人类超越了那些思想和行动的有限<br />
圈子。这种超越土地限制，度过大海的活动，是亚细亚洲各国所没有的。<br />
<br />
　　今天的中国青年，也许会责怪我们的祖先：你们为什么那么眷恋大陆，始终？<br />
~ 能超越土地的限制走向大海呢？<br />
<br />
　　这就是历史的命运。<br />
<br />
　　至迟在大约八千年前，农耕文化在黄河岸边就诞生了。从四处狩猎到固定在<br />
某一片土地上，文明所迈出的关键的一步，据说是从一只采集种子的妇女的手开<br />
始的。<br />
<br />
　　摆脱野蛮人的第一个代价，就是被牢牢地栓在土地上，难道我们的祖先能不<br />
这样选择吗？<br />
<br />
　　更不可选择的是，黄河中下游这块文明的摇篮地，偏偏又处在一种很独特的<br />
地理环境中。（演播室。学者谈中国地理环境特徵。）<br />
<br />
　　冯天瑜（湖北大学历史系教授）：黄河中下游作为中华文化的核心地带，它<br />
的北边是比较难以逾越的蒙古戈壁，西北是万里黄沙，形成交通障壁。西南是世<br />
界上最高大最险峻的青藏高原。东边面临地球上最大的海洋――太平洋，它的浩<br />
瀚无际跟地中海的情形不一样，对古人来说也是难以征服的，这么看来，地理环<br />
境对以黄河流域为中心的中华文化形成了一种隔绝机制，造成了一种内向的，求<br />
稳定的文化类型。<br />
<br />
　　因此，中国人既不像欧洲民族那样生活在地中海周围，也不像美国人那样住<br />
在两个大洋之间。命运就给中国人安排了这样一种生存空间。<br />
<br />
　　几千年来，肥沃的中原地区始终面对著北方那个广袤纵深的蒙古高原，这种<br />
平原与高原的直接对峙，在欧洲是不存在的，它形成了某种奇特的历史关系：处<br />
于迁徙无定状态中的高原游牧民族，始终把平原大河流域，作为他们争夺的一个<br />
目标，经常像洪水一样从高原上横冲下来。整个中国古代史，几乎就是一部游牧<br />
人同农耕人争夺生存空间的历史。<br />
<br />
　　因此，直到封建社会末期，明清之际思想家王夫之还对华夏农业文明充满了<br />
一种文化上的自豪。他不无鄙夷地嘲笑夷狄的游牧文化，还处在「逐水草，习射<br />
猎，忘君臣，略婚宦，驰突无恒」的低级阶段，而在中原地区，则「有城廓之可<br />
守，墟市之可利，田土之可耕，赋税之可纳，婚姻仕进之可荣」。在工业文明出<br />
现以前，谁能否认这样的华夏农业文明的先进性呢？理所当然，中原人是必须保<br />
护它不受游牧文化的侵扰的。<br />
<br />
　　最好的保护手段，莫过于城廓。<br />
<br />
　　在山西临潼姜寨出土的原始村落遗址，也许是最早的城廓雏形。你看，所有<br />
的房门都朝向中心广场，村落只朝东方留著通路，这种布局，明显地突出了团结<br />
向心的精神。<br />
<br />
　　当我们再俯瞰北京城时，会惊讶地发现某种六千年的一致性。<br />
<br />
　　后来，有了城墙。<br />
<br />
　　到战国时期，这城墙又扩大到国境线上。<br />
<br />
　　把现有的明长城再向外推进伍佰到一千华里，在阴山和贺兰山脉一线，就是<br />
当年秦始皇命蒙恬修筑的长城，也就是传说中的孟姜女哭骂的那个长城。<br />
<br />
　　这是人类历史上最浩大的工程，而指导这个工程的全部思想，早在数千年前，<br />
已经由姜寨部落的首领发明了。<br />
<br />
　　有了城防，对外可以抵挡游牧民族的劫掠，对内则产生一种凝聚力，把城内<br />
的人民压向一个权力核心。因此，谁修了长城，谁好像就拥有了长城以内的土地，<br />
山河与人民，长城也就成了他家的院墙。<br />
<br />
　　然而，在爱琴海边，一个西方的千古一帝亚历山大，早已率领著他那所向披<br />
靡的马其顿大军，远离自己的祖国，横扫了欧亚非各个古老帝国。仿佛同亚历山<br />
大的东征前呼后应，秦始皇也开始了大规模抗击匈奴的战争。这位千古一帝，决<br />
不像亚历山大那样四处游荡，而是按照东方帝王特有的思维习惯和想向力，修筑<br />
了亚历山大做梦都想不到的万里长城。　秦始皇的这种伟大的想像力，仍然是一<br />
种不能超越土地的想像力。<br />
<br />
　　到了公元一零二年，追击匈奴直达中亚腹地的一位中国汉朝将军班超，为了<br />
窥视罗马帝国的虚实，派出他的副将甘英西渡波斯湾。然而，甘英却被海浪吓退<br />
了。<br />
<br />
　　从亚历山大的东征，到班超的西渡，历史走过了将近四百年，由于高山和大<br />
海的阻隔，东西两大帝国所代表的两大文明，在历史的邂逅中两度失之交臂。那<br />
种直接的对抗和融合，征服和反征服，同化和反同化所可能激起的强烈火花，终<br />
于没有在历史的大舞台上闪现。<br />
<br />
　　几千年来，中国人在这块土地上日出而作，日落而息，因此，对于节气这样<br />
的大的时间座标，中国人也习惯于把百年兴衰，只看作是历史长河的短暂的一瞬<br />
间。盛衰的交替，犹如冬去春来。　多么重大的社会变动和人间灾难，也似乎是<br />
过眼烟云。　世界上没有哪个民族像中国人那样具有深刻的历史感。但同时，也<br />
都不象中国人那样奉行著一种独特的，听天由命的生命哲学。<br />
<br />
　　长城就把这个宁静的，熟透了的农耕文明紧紧地包裹起来。久而久之，它就<br />
变得再也不会像秦皇汉武那样去主动出击了。<br />
<br />
　　然而，北方那个沉默的蒙古高原常常会突然苏醒过来。<br />
<br />
　　高原是凝固封闭的，不易到达的，但它却也容易把内部积聚起来的力量和冲<br />
动送到平原上来。　一旦遇到乾旱，游牧人的牲畜大批死亡，他们就会统一起来，<br />
冲下高原，邻近的农业文明就灾难临头了。<br />
<br />
　　当成吉思汗凶猛的骑兵潮水般涌来的时候，别说长城，就连黄河长江这样的<br />
天堑，也低档不住。<br />
<br />
　　北中国的大地，几度桑田，几度牧场。　长城南北，几番征战，几多白骨。<br />
忽而是汉妾辞宫，公主和亲，忽而是番王来朝，纳供称臣。　多少历史悲喜剧，<br />
在长城的巨大背景下轮番演出。如果说秦皇汉武修长城，还表现了华夏文明的气<br />
魄和力量的话，那么，到了十五世纪中叶明朝重修长城，就完全成为一种失败和<br />
退缩的举动了。<br />
<br />
　　这条一万一千华里的砖石砌成的明长城，比起秦汉长城来，自然是牢固多了。<br />
然而，它也使明朝耗尽力量，元气大伤。　等到女真人崛起于白山黑水之间，一<br />
代雄杰努尔哈赤挥戈南下的时候，这绵延数万里的砖石长城，只能再一次记录巨<br />
大的失败了。以致于后来的康熙皇帝说：修筑长城，实属无益。<br />
<br />
　　古北口的这一段雄伟的长城，是由一代名将戚继光戚继光镇守苏州时督建的。<br />
<br />
　　这位「一年是三百六十天，多是横戈马上行」的名将，被史学家称为「孤独<br />
的将军」。他不但修建了北国的第一段长城，为了抗击倭寇，他还曾在海边修了<br />
一座海岸长城――那就是著名的蓬莱水城。　中国的第一支海军，就守在城墙的<br />
后面。<br />
<br />
　　毫无疑问，戚继光是明代最有天才的军事家。不过，他留给我们的遗憾也是<br />
巨大的：为什么岛国的倭寇可以渡过海洋来大中国，而中国人只能守在海边，竟<br />
然连想也没想过去那个岛国看看这倭寇究竟是怎么会事？为什们当时的欧洲已经<br />
拥有火器装备的海军四处侵略，而中国还只知道修筑万里长城？并且竟然把长城<br />
修到了海边呢？<br />
<br />
　　公元一五八八年初，将星殒落，戚继光在贫病交加中死去。长城，随著戚继<br />
光的去世，不再有任何意义。<br />
<br />
　　与此同时，西班牙的无敌舰队，正整装待发，出征英国，去揭开近代世界历<br />
史上轰轰烈烈的一页。<br />
<br />
　　人们还记得秦始皇修建的古长城吗？如今它还沉睡在沙漠之中。　茫茫流沙<br />
从北方一步蚕食过来，狂风雕塑著它，仿佛它是一个千年的流放者，躺在这荒漠<br />
之中，凝固成一个没有答案的沉思。<br />
<br />
　　与秦长城的被遗忘相反，想后退缩了一千华里的明长城却受到了无比的崇仰。<br />
人们为它是地球上唯一能被登月宇航员看到的人类工程而自豪。人们甚至硬要用<br />
它来象徵中国的强盛。然而，假使长城会说话，它一定会老老实实告诉华夏子孙<br />
们，它是由历史的命运所铸造的一座巨大的悲剧纪念碑。它无法代表强大，进取<br />
和荣光，它只代表著封闭，保守，无能的防御和怯弱的不出击。　由于它的庞大<br />
和悠久，它还把自诩自大和自欺欺人深深地烙在了我们民族的心灵上。呵，长城，<br />
我们为什么还要呕歌你呢？<br />
<br />
　　学者们已经发现，绵延万里的长城，正好同十五英寸降水线大致重合。这条<br />
降水线，正好由意味著农业和非农业。<br />
<br />
　　今天，在宁夏红石峡长城之上，我们还能看到我们祖先留下的「华夷天堑」<br />
的石碑。这的确是农业文明的最后边界。一切都是明明白白的。我们的祖先永远<br />
无法超越土地和农业。他们最奇伟的想象和最大胆的举动，都只能是修长城。<br />
<br />
　　历史上大约还不曾有过像明太祖朱元璋这样一位要把百姓牢牢绑在土地上的<br />
皇帝。他一再申令「不许片板下海」，这固然包含著国防的目的，但他也深深懂<br />
得，只有把全体人民牢牢地捆缚在土地上，他的王朝才是巩固的，他对离开土地<br />
的人深痛恶绝，一律迁之远方，明朝法律规定，任何人外出必须持有证件，否则<br />
关卡查获立即送官。<br />
<br />
　　流动，迁徙，贸易都被窒息。土地和专制把中国人捆死了，几百年下来，中<br />
国人怎么还能懂得自由和贸易呢？<br />
<br />
　　十五世纪对于整个人类来说，是非常关键的一个世纪。人类开始把眼睛从大<br />
陆移向海洋。　不管是对东方，还是对西方，历史都公平地让它们进行一次选择。<br />
　无论是太平洋，印度洋，还是大西洋，都对大陆上的民族敞开著胸膛。<br />
<br />
　　站在十五世纪的门槛上面对著大海，这个在大陆上待惯了的华夏民族，将作<br />
出怎样的选择呢？<br />
<br />
　　公元一四零五年，一支十五世纪全世界无与伦比的庞大的船队，乘著强劲的<br />
东北季候风，浩浩荡荡离开福建五虎门，在历史给予的大选择前，率先驶向了太<br />
平洋。一直到今天，人们还在对这支船队的远航目的猜测纷纭。<br />
<br />
　　由郑和率领的这支船队，前后二十八年中，七下西洋，足迹遍于东南亚和南<br />
亚，又横渡印度洋，航程远达阿拉伯和东非海岸。<br />
<br />
　　然而，人类历史还不曾有过这样一次毫无经济目的的大规模航海活动。它是<br />
一次几乎纯而又纯的政治游行，它要施恩于海外诸国，以表达中国皇帝对它们名<br />
义上的最高宗主权。多么慷慨温和的君子国行为呵。黑格尔说，大海邀请人类从<br />
事征服和贸易。可是，太平洋邀请来的中国人，竟是所谓「正其谊而不谋其利」<br />
的谦谦君子。中国人即使来到海上也还是不能超越陆地上那种有限的思想和行动<br />
和圈子。历史选择了中国人，而中国人却不能选择历史。仅仅几十年后，代表著<br />
弱小的资本主义的四艘小帆船，葡萄牙人达迦马的率领下，为寻找财富和市场驶<br />
入了印度洋。那时，庞大的郑和船队已经从太平洋和印度洋上消失的无影无踪了。<br />
而欧洲人却开始了地理大发现的伟大探险。<br />
<br />
　　这既是一种历史的巧合，也是一种历史的必然。<br />
<br />
　　亚细亚，这个太阳升起的地方，这个世界历史的创始之地，由于这次历史大<br />
选择的坐失良机，太阳将不再升起。<br />
<br />
　　曾经在这个星球上遥遥领先中国文明，也因此不得不接受屈辱和被动的命运。<br />
<br />
　　将近五百年后，北洋水师在黄海，与日本海军展开的甲午大海战中全军覆没。<br />
无论是丁汝昌还是邓世昌，他们的悲剧性的失败，其实早在戚继光和郑和的时代<br />
就已经注定了。<br />
<br />
　　几千年来，中国东南方的太平洋一直是沉默的。　一旦太平洋的狂涛，载著<br />
西方列强的军舰，和比军舰更有威力的新思想新文化呼啸而来的时候，中国人已<br />
经没有还手之力了。<br />
<br />
　　这来自西方得及海啸，决不象过去从蒙古高原洪水般冲决下来的游牧文化，<br />
泛滥一阵便很快退的无影无踪。　海上来的是一种新文明，古老的华夏农业文明<br />
再也不可能同化它了。于是，种族危亡和文明危机同时爆发了。<br />
<br />
　　救民族之危亡，势必拒寇于国门之外，但是，救文明之衰微，又必须打开国<br />
门，对外开发，迎接科学和民主的新曙光。这极为矛盾的救亡与近代化的双重变<br />
奏，近几百年来交替书写著中国畸形的历史，真是错综复杂，头绪万分，剪不断，<br />
理还乱，让中国人付出了无数沉重的代价代价！古老而孱弱的农业文明，逼得我<br />
们的祖先只知道用靡费而不中用的长城来保卫自己的果实，逼得他们即时走到海<br />
上也不懂得贸易和竞争。这种文明的萎缩，如今已经萎缩著整个华夏民族的生命<br />
力和创造力，我们再也不能失去命运赐予的任何一次机会了。<br />
<br />
　　今天，我们已经变得聪明多了。<br />
<br />
