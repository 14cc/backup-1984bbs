																											<div id="postmessage_15912" class="t_msgfont">灰烬中的钻石：电影《卡廷森林》<br />
王怡<br />
<br />
<a href="http://www.artblog.cn/U/joshuawang/archives/2008/39939.html" target="_blank">http://www.artblog.cn/U/joshuawang/archives/2008/39939.html</a><br />
<br />
<img src="http://www.economist.com/images/columns/2007w39/KatynAFP.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
（导演瓦伊达和他的海报）<br />
<br />
82岁的瓦伊达，是比基耶夫洛夫斯基更伟大的波兰导演。奥斯卡在2000年把终生荣誉奖给了他。柏林电影节拖到2006年，也向他颁出终生成就奖。谁又料想，他的风烛残年，还能拍出恢宏史诗呢。尽管今年的金熊和奥斯卡都舍不得再把荣誉给他。但在我眼里，《卡廷森林》无疑是近十年最伟大的电影。<br />
<br />
伟大到令我手足无措的地步。无论钻石还是灰烬，都超出我的写作能力，并叫我的词语山穷水尽。开头只十分钟，一个镜头，一句对白，已让灵魂起伏不已。1939年，苏联配合纳粹，入侵波兰，将两万多名被俘的波兰军官押入集中营。妻子在教堂的临时抢救点寻找丈夫。一位神父跪在尸体之间。她女儿喊道，那是爸爸的军装。妻子跑过去，掀开盖在头上的大衣，下面却是一具被毁坏了的、戴着荆棘冠冕的耶稣像。那一刻，我和她同时被震骇。神父伸手，轻轻将耶稣像重新盖上。<br />
<br />
这突如其来的一幕，为电影奠定了一种令人战兢的崇高感。1989年之后，瓦伊达和波兰人民等了将近20年，来描述这个曾被谎言遮盖了50年的惨案。但在瓦伊达这里，苦难是这样被描述的。那一场苦难中，最深沉的真相，不是我们被苏联人屠杀，而是基督被人类重新钉上了十字架。<br />
<br />
当妻子跨越大半个波兰，在波兰军官团被押送之前，找到她的丈夫，波兰最年轻的骑兵上尉。她劝他逃走，丈夫拒绝了。妻子绝望地说，你曾以上帝的名义说出誓言，与我联合，成为一体，直到死亡将我们分开。上尉说，可我对波兰也有誓言，我无法背弃军人的使命。<br />
<br />
史诗的伟大，在于它依靠的不是悬念。因为你一开头就知道他们的结局。但这部电影的重心，是每个人的定意与抉择。《旧约》说，人的灵魂是上帝的形像。《新约》解释说，这形像就是“真理的仁义和圣洁”。至于灵魂的内涵，康德的三分法是一个被普遍接受的理论，就是情感、思想和意志。当人的意志顺服真理时，人就胜过处境，葆有了他的尊严。<br />
<br />
上尉的父亲是大学教授，当纳粹召集教授集合时，妻子也劝他不要去。父亲和儿子一样，说我一定要去，要让德国人知道我们是站在一起的。1943年，德国人发现了卡廷森林的万人坑，全体波兰军官在1940年4月，逐一被手枪近距离从后脑勺枪杀，纳粹的纪录片说，“这是典型的苏联克格勃的手法”。希特勒向被杀害的波兰将军夫人颁发了勋章，要录下她宣读的声明，向全国广播。镜头在将军夫人的脸上几乎停顿了几十秒，纳粹威胁说，不然送你的女儿去奥斯维辛。她轻轻推开那份声明，走出房门，晕倒在街上。<br />
<br />
二战结束后，波兰被苏共控制。苏联重新制作了纪录片，声称1941年纳粹占领卡廷后，屠杀了波兰战俘。近距离从后脑勺射击，“这是典型的德国党卫队的手法”。高音喇叭说，每一具埋在卡廷的尸体，都在控诉着帝国主义的罪恶。从此，波兰人被迫活在一个巨大和高压的谎言里。卡廷，成为良心的试金石。一位高中毕业生在申请大学的简历里说，父亲1940年被苏联杀死。老师叫他改掉。可是，要在父亲的死上说谎，是我读大学的条件吗――是的，我们要重建波兰，如果连你们也被杀了，波兰的未来在哪里呢。<br />
<br />
善恶的区别永远最简单，谎言的自我辩护都极其复杂。可爱的孩子只说了一句话，“一个人一生只有一份简历”。老师交换意见，感叹说，你对卡廷事件的态度，决定了你对波兰人民的忠诚。二十分钟后，这位年轻人死在街头。<br />
<br />
一位姐姐，为上尉飞行员的弟弟定制了墓碑。上面写着，“1940年死于卡廷”。她以散播谣言的罪名被捕。之前她和弟媳讨论入党的事。她说，你决定入党，在新世界找到了位置，但我仍然在弟弟的世界里。如果我必须选择，我选择和他在一起。<br />
<br />
那你选择了死亡――不，我选择被害者一边，而不是杀人犯一边。<br />
<br />
电影中，四位军官的亲友们，都在艰难的生活中做出不同的选择。那位骑兵上尉在最后一批枪杀中，顶替了他的朋友。这位死里逃生的朋友，选择了沉默，在波共政权下升为上校。他与那位将军夫人有一次对话，讲述自己忍辱负重的理由。夫人一针见血地说，也许你的想法和他们不同，但你的做法是一样的。你想法不同又有什么用呢。<br />
<br />
这话的意思，是一个绝对的善恶标准，在我们的意志之上。是非之心，也刻在人的心上。上校最终吞抢自杀了。就如瓦伊达50年前打破沉默的名作《灰烬与钻石》，选择顺服，就是选择灰烬中的钻石；选择悖逆，就是选择埋藏钻石的灰烬。<br />
<br />
卡廷惨案，使波兰在二战后落入彻底的荒谬。但如那位妻子所说的，只有死亡才能将一个誓言分开。意思是说，苦难和谎言都没有这个能力。哪怕苦难再大，哪怕谎言重复一千遍，或重复了50年。<br />
<br />
因为活在谎言中，根本就不是活着。<br />
<br />
1991年12月23日，戈尔巴乔夫移交政权时，邀请叶利钦共同阅读了总统密档第一卷。他在回忆录中说，“当时我头发都竖起了”。密档有三份文件，即1940年3月5日贝利亚给斯大林的报告，及斯大林等签署的命令，同意秘密处决波兰战俘共25700人。命令中说“审案时，不传唤囚犯、不提起公诉、不出示侦查终结书和判决书”。另一份是1959年3月3日，克格勃首脑给赫鲁晓夫的报告，统计卡廷屠杀的总人数为21857人，并建议“销毁全部档案”。<br />
<br />
1992年10月14日，叶利钦遣特使，去华沙，将三份密档的副本转交给了波兰总统瓦文萨，结束了维持半个世纪的谎言。<br />
<br />
被推土机埋在卡廷森林的军官中，也有瓦依达的父亲。没拍这部影片之前，他的电影世界始终是残缺的。50多年来，瓦依达用各种寓言讲述一个苦难的波兰，直到他拍出这一段、不能不拍的历史的灰烬。<br />
<br />
但灰烬中找不到钻石，除非跪下来祈祷。影片最后的屠杀场面，无论你近一百分钟的心理准备如何，依然具有意想不到的震撼力。当一个接一个波兰军官，面对万人坑，被枪杀之前诵读主祷文：<br />
<br />
我们在天上的父――枪响；<br />
<br />
