													<h2>《红色纪念碑》：广西“人吃人事件”调查（下载）</h2>
						
						
																											<div id="postmessage_490479" class="t_msgfont">郑义：红色纪念碑<br />
作者：郑义 文章来源：本站原创 点击数：7840 更新时间：2009-1-14 21:31:13&nbsp;&nbsp;<br />
<br />
为什么看起来不像魔鬼的寻常人家，却在那种群体的疯狂中堕落到连禽兽都不如？为了要探究文革发生在广西的大规模屠杀与人吃人恐怖事件，知名作家郑义两次深入广西调查，完成《红色纪念碑》一书。该书公开出版十多年来，没人敢说“不”字，……。<br />
<br />
1986年夏，郑义第一次到广西省调查“人吃人事件”，他查阅档案，察勘现场，采访相关官员、在押犯人、当年凶手、被害者遗属、目击证人，搜集了大量档案材料与证人证词。1988春第二次赴广西省调查，补充了第一次调查的遗缺。<br />
<br />
1989年后近三年的时间里，郑义完成了《红色纪念碑》（ScarletMemorial）这本重要著作，在全世界引起轰动。<br />
<br />
《红色纪念碑》<br />
<br />
《红色纪念碑》由两个部份组成：调查广西省范围内的档案卷宗和采访幸存者、凶犯、受难者遗属。郑义调查并采访了发生过大规模屠杀与人吃人恐怖事件的五个县。仅仅宾阳县，在1968年7、8月的“红色风暴”期间，共有3，681人被枪杀、戳死、勒死、叉死、溺死、砸死，甚至活埋。<br />
<br />
在武宣县境内，滋事者从活人身体内割下心脏和肝脏，然后煮而食之。<br />
<br />
“当受难者被推上街头游行批斗时，老太太们会提着菜篮子守候。一当受害者被处死，众人蜂拥而上。那些冲在前面的人将会得到一块好肉。”一个老太太养成了专挖眼睛的习惯，认为吃了它们会增进她自己的视力。另一个年青女干部，一旦可能，要消受的是男性生殖器。在一个中学里，学生吃掉了他们的老师。<br />
<br />
关于广西吃人事件。郑义将其分为三个阶段：<br />
<br />
1.开始阶段：其特点是偷偷摸摸，恐怖阴森。某县一案卷记录了一个典型场面：深夜，杀人凶手们摸到杀人现场破腹取心肝。由于恐怖慌乱，加之尚无经验，割回来一看竟是肺。只有战战兢兢再去。……煮好了，有人回家提来酒，有人找来佐料，就着灶口将熄的火光，几个人悄悄地抢食，谁也不说一句话。……<br />
<br />
2.高潮阶段：大张旗鼓，轰轰烈烈。此时，活取心肝已积累了相当经验，加之吃过人肉的老游击队员传授，技术已臻于完善。譬如活人开膛，只须在软肋下用刀拉一“人”字形口子，用脚往肚子上一踩，（如受害者是绑在树上，则用膝盖往肚子上一顶――）心与肚便豁然而出。为首者割心、肝、生殖器而去，余下的任人分割。红旗飘飘，口号声声，场面盛大而雄壮……<br />
<br />
3.群众性疯狂阶段：其特点可以一句话概括：吃人的群众运动。如在武宣，像大疫横行之际吃尸吃红了眼的狗群，人们终于吃狂吃疯了。动不动拖出一排人“批斗”，每斗必吃，每死必吃。人一倒下，不管是否断气，人们蜂拥而上，掣出事先准备好的菜刀匕首，拽住哪块肉便割哪块肉。……至此，一般群众都卷入了吃人狂潮。那残存的一点罪恶感与人性已被“阶级斗争的十二级台风”刮得一干二净。吃人的大瘟疫席卷武宣大地。其登峰造极之形式是毫无夸张的“人肉筵席”：将人肉、人心肝、人腰子、人肘子、人蹄子、人蹄筋……烹、煮、烤、炒、烩、煎，制作成丰盛菜肴，喝酒猜拳，论功行赏。吃人之极盛时期，连最高权力机构――武宣县革命委员会的食堂里都煮过人肉！<br />
<br />
……<br />
<br />
调查报告完全是真实的<br />
<br />
记者：就是说，您这是调查报告，完全是以事实为基础？<br />
<br />
郑义：当然是调查报告，每句话都必须要有出处的，是以事实为依据的。为了把情况调查清楚，我到基层时，有几方面的人，我都尽量要见的：<br />
<br />
第一，是当地公检法方面的人。在他们的档案里，有犯罪的具体记录，这些档案是有法律效力的；<br />
<br />
第二，尽可能采访当年受迫害者和揭露迫害的地方官员。文革不只是普通群众受迫害，共产党内部意见不一样的官员也受到了迫害。这些人都是有名有姓的，他们有一定地位，说话也比较慎重，所以提供的材料也是比较可信的；<br />
<br />
第三，我还要去见当年杀人和吃人的凶手。他们有些人年纪很大了，也没有判刑，还在自己家里住着，我就跑到农村去见他们。还有一些关在监狱里，我就到监狱去访问他们。<br />
<br />
第四，就是去见当年被屠杀或被吃掉的受害者的遗属，听听他们怎么说的，因为他们是苦主。<br />
<br />
出版十多年　无人敢说“不”<br />
<br />
总之，只要是能够向我提供确凿、可信证据的人，我基本上都见了。我没有发现一个人对我撒谎。从不同渠道得到的资料，没有任何矛盾之处，这是非常不容易的。……<br />
<br />
我的关于广西人吃人的著作，如《红色纪念碑》，基本上是以这个调查为基础来写。这些书公开出版十多年来，没有任何人敢出来说一个“不”字，包括共产党，在这个问题上，一句话也不敢多说。<br />
<br />
在民间，有持不同意见的人说：你把这种事情写出来，不是丢我们中国人的脸吗？我说，不是丢中国人的脸。比如，揭发德国纳粹、日本军国主义的罪恶，并不是说德国、日本就没有好人了，不是这个道理。<br />
<br />
……<br />
<br />
这么多年来，没有人敢出来否定，这得益于当初向我提供证据的是真实的，我写的也是真实的，绝对不敢有任何想像和添加。<br />
