第一部分<br />
序言<br />
　　“香港胜在有ICAC”<br />
<br />
　　香港并非到处可见标语口号，但是“香港胜在有ICAC”这个标语却家喻户晓、深入人心。ICAC是廉政公署的英文名称IndependentCommissionAgainstCorruption的缩写，这个口号也反映了香港语文的特点，即中英并用，朗朗上口。<br />
&nbsp; &nbsp; &nbsp; &nbsp; <br />
　　其实香港并非早就有廉署；廉署是伴随着香港经济的起飞与社会的发展而建立和成长的，是香港民众抗议腐败特别是警方腐败、向港英当局施加压力的结果，也是香港这个国际都会在特殊的时空环境中形成的制度性反腐利器。廉政公署的英文名称的意思是“独立反贪公署”，“独立”乃是这一机构的精粹。在回归前，廉署的中文名称是“总督特派廉政专员公署”，顾名思义，廉政专员直属总督，只听命于总督一人，专门从事公私企业机构的反贪任务，拥有极大的相应权力。笔者从内地移居香港近20年了，虽然没有和廉署直接打交道的经验，但是作为一个香港居民，对于香港社会的法治和文明程度，却有着深刻而直接的感受。香港的法治与文明，在很大程度上得力于廉政公署这一独特的机构。<br />
<br />
　　德国的“透明国际”是世界上较早成立也较有权威的研究国际廉政的非政府机构，每年都发布世界各国（地区）廉政的排行榜。“透明国际”从1995年成立以来发布的廉政排行榜，香港一直名列前茅，由此可以看出香港的廉政程度在国际社会享有很高的声誉，其中廉政公署当然发挥了关键作用。<br />
<br />
　　“廉署请喝咖啡”，这句话在香港人尽皆知，意思当然不是廉署的咖啡特别香醇，而是意指廉署要请人协助调查，也就是被邀者已经被列入廉署的调查名单了。这是咖啡爱好者没有想到的。在香港，铁面无私、执法如山可谓廉政公署的同义词，因为廉署成立所办的案子特别是那些著名的大案，充分说明了廉署作为维护法治的一个强力部门的权威性，廉署在香港社会令人敬畏的名声，也就因此而来。<br />
<br />
　　廉署在回归后，根据基本法继续执行着其法治守护神的功能，其名声也远播内地，引起了内地媒体和民众的极大兴趣。<br />
<br />
　　本书以香港廉政公署成立以来的社会发展为背景，廉署所成功侦办的多起惊心动魄的大案为经纬，全面揭示廉署的历史和作用，使读者一书在手，可以了解为什么“香港胜在有ICAC”，可以了解在香港这样一个发达的资本主义社会，为什么廉政程度比较高，为什么廉署使贪赃枉法者闻风丧胆。<br />
<br />
　　不过在近年来，廉署的一些行动在香港也引起了争议，尤其是廉政公署对待不同政党采取双重标准，以及廉署2004年一口气查抄了多家报社这些事件，都引起舆论的强烈批评；香港法院在一起案件中判决廉署窃听嫌疑人的电话违反基本法，也使廉署的侦察行动受到约束。在廉署内部，1993年廉署高层、执行处副处长徐家杰突然被解雇；2005年夏天廉署第三把手突然辞职，以及其内部人员较高的流失率，也引起了舆论的广泛关注。为了帮助读者尽可能全面深入廉署，在书中也据实记载了这方面的资料，读者可以自行判断。</div>

							
							
							
							
