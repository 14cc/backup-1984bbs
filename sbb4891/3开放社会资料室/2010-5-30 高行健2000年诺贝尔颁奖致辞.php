																											<div id="postmessage_648731" class="t_msgfont">我不知道是不是命运把我推上这讲坛，由种种机缘造成的这偶然，不妨称之为命运。上帝之有无且不去说，面对这不可知，我总心怀敬畏，虽然我一直自认是无神论者。&nbsp;&nbsp;<br />
<br />
　　一个人不可能成为神，更别说替代上帝，由超人来主宰这个世界，只能把这世界搅得更乱，更加糟糕。尼采之后的那一个世纪，人为的灾难在人类历史上留下了最黑暗的纪录。形形色色的超人，号称人民的领袖、国家的元首、民族的统帅，不惜动用一切暴力手段造成的罪行，绝非是一个极端自恋的哲学家那一番疯话可以比拟的。我不想滥用这文学的讲坛去奢谈政治和历史，仅仅藉这个机会发出一个作家纯然个人的声音。&nbsp;&nbsp;<br />
<br />
<br />
　　作家也同样是一个普通人，可能还更为敏感，而过于敏感的人也往往更为脆弱。一个作家不以人民的代言人或正义的化身说的话，那声音不能不微弱，然而，恰恰是这种个人的声音倒更为真实。&nbsp;&nbsp;<br />
<br />
<br />
　　这里，我想要说的是，文学也□能是个人的声音，而且，从来如此。文学一旦弄成国家的颂歌、民族的旗帜、政党的喉舌，或阶级与集团的代言，尽管可以动用传播手段，声势浩大，铺天盖地而来，可这样的文学也就丧失本性，不成其为文学，而变成权力和利益的代用品。&nbsp;&nbsp;<br />
<br />
<br />
　　这刚刚过去的一个世纪，文学恰恰面临这种不幸，而且较之以往的任何时代，留下的政治与权力的烙印更深，作家经受的迫害也更甚。文学要维护自身存在的理由而不成为政治的工具，不能不回到个人的声音，也因为文学首先是出自个人的感受，有感而发。这并不是说文学就一定脱离政治，或是文学就一定干预政治，有关文学的所谓倾向性或作家的政治倾向，诸如此类的论战也是上一个世纪折腾文学的一大病痛。与此相关的传统与革新，弄成了保守与革命，把文学的问题统统变成进步与反动之争，都是意识形态在作怪。而意识形态一旦同权力结合在一起，变成现实的势力，那么文学与个人便一起遭殃。&nbsp;&nbsp;<br />
<br />
<br />
　　二十世纪的中国文学的劫难之所以一而再，再而三，乃至于弄得一度奄奄一息，正在于政治主宰文学，而文学革命和革命文学都同样将文学与个人置于死地。以革命的名义对中国传统文化的讨伐导致公然禁书、烧书。作家被杀害、监禁、流放和罚以苦役的，这百年来无以计数，中国历史上任何一个帝制朝代都无法与之相比，弄得中文的文学写作无比艰难，而创作自由更难谈及。&nbsp;&nbsp;<br />
<br />
<br />
　　作家倘若想要赢得思想的自由，除了沉默便是逃亡。而诉诸言语的作家，如果长时间无言，也如同自杀。逃避自杀与封杀，还要发出自己个人的声音的作家不能不逃亡。回顾文学史，从东方到西方莫不如此，从屈原到但丁，到乔伊斯，到托马斯．曼，到索忍尼辛，到一九八九年后中国知识分子成批的流亡，这也是诗人和作家还要保持自己的声音而不可避免的命运。&nbsp;&nbsp;<br />
<br />
<br />
　　在毛泽东实施全面专政的那些年代里，却连逃亡也不可能。曾经蔽护过封建时代文人的山林寺庙悉尽扫荡，私下偷偷写作得冒生命危险。一个人如果还想保持独立思考，只能自言自语，而且得十分隐秘。我应该说，正是在文学做不得的时候我才充分认识到其所以必要，是文学让人还保持人的意识。&nbsp;&nbsp;<br />
<br />
<br />
　　自言自语可以说是文学的起点，藉语言而交流则在其次。人把感受与思考注入到语言中，通过书写而诉诸文字，成为文学。当其时，没有任何功利的考虑，甚至想不到有朝一日能得以发表，却还要写，也因为从这书写中就已经得到快感，获得补偿，有所慰藉。我的长篇小说《灵山》正是在我的那些已严守自我审查的作品却还遭到查禁之时著手的，纯然为了排遣内心的寂寞，为自己而写，并不指望有可能发表。&nbsp;&nbsp;<br />
<br />
<br />
　　回顾我的写作经历，可以说，文学就其根本乃是人对自身价值的确认，书写其时便已得到肯定。文学首先诞生于作者自我满足的需要，有无社会效应则是作品完成之后的事，再说，这效应如何也不取决于作者的意愿。&nbsp;&nbsp;<br />
<br />
<br />
　　文学史上不少传世不朽的大作，作家生前都未曾得以发表，如果不在写作之时从中就已得到对自己的确认，又如何写得下去？中国文学史上最伟大的小说《西游记》、《水浒传》、《金瓶梅》和《红楼梦》的作者，这四大才子的生平如今同莎士比亚一样尚难查考，□留下了施耐庵的一篇自述，要不是如他所说，聊以自慰，又如何能将毕生的精力投入生前无偿的那宏篇钜制？现代小说的发端者卡夫卡和二十世纪最深沉的诗人费尔南多．毕索瓦不也如此？他们诉诸语言并非旨在改造这个世界，而且深知个人无能为力却还言说，这便是语言拥有的魅力。&nbsp;&nbsp;<br />
<br />
　　「作家倘从事这种写作，显然难以为生，不得不在写作之外另谋生计，因此，这种文学的写作，不能不说是一种奢侈，一种纯然精神上的满足。这种冷的文学能有幸出版而流传在世，只靠作者和他们的朋友的努力。曹雪芹和卡夫卡都是这样的例子。他们的作品生前甚至都未能出版，更别说造成甚么文学运动，或成为社会的明星。这类作家生活在社会的边缘和夹缝里，埋头从事这种当时并不指望报偿的精神活动，不求社会的认可，只自得其乐。」&nbsp;&nbsp;<br />
<br />
<br />
　　「冷的文学是一种逃亡而求其生存的文学，是一种不让社会扼杀而求得精神上自救的文学，一个民族倘竟容不下这样一种非功利的文学，不仅是作家的不幸，也该是这个民族的悲哀。」&nbsp;&nbsp;<br />
<br />
<br />
　　我居然在有生之年，有幸得到瑞典文学院给予的这巨大的荣誉与奖赏，这也得力于我在世界各地的朋友们多年来不计报酬，不辞辛苦，翻译、出版、演出和评介我的作品，在此我就不一一致谢了，因为这会是一个相当长的名单。&nbsp;&nbsp;<br />
<br />
<br />
　　我还应该感谢的是法国接纳了我，在这个以文学与艺术为荣的国家，我既赢得了自由创作的条件，也有我的读者和观众。我有幸并非那么孤单，虽然从事的是一种相当孤独的写作。&nbsp;&nbsp;<br />
<br />
<br />
　　我在这里还要说的是，生活并不是庆典，这世界也并不都像一百八十年来未有过战争如此和平的瑞典，新来临的这世纪并没有因为经历过上世纪的那许多浩劫就此免疫。记忆无法像生物的基因那样可以遗传。拥有智能的人类并不聪明到足以吸取教训，人的智能甚至有可能恶性发作而危及到人自身的存在。&nbsp;&nbsp;<br />
<br />
<br />
　　人类并非一定从进步走向进步。历史，这里我不得不说到人类的文明史，文明并非是递进的。从欧洲中世纪的停滞到亚洲大陆近代的衰败与混乱乃至二十世纪两次世界大战，杀人的手段也越来越高明，并不随同科学技术的进步人类就一定更趋文明。&nbsp;&nbsp;<br />
<br />
<br />
　　以一种科学主义来解释历史，或是以建立在虚幻的辩证法上的历史观来演绎，都未能说明人的行为。这一个多世纪以来对乌托邦的狂热和不断革命如今都尘埃落地，得以幸存的人难道不觉得苦涩？ <br />
<br />
<br />
　　否定的否定并不一定达到肯定，革命并不就带来建树，对新世界的乌托邦以铲除旧世界作为前提，这种社会革命论也同样施加于文学，把这本是创造的园地变为战场，打倒前人，践踏文化传统，一切从零开始，唯新是好，文学的历史也被诠释为不断的颠覆。&nbsp;&nbsp;<br />
<br />
<br />
　　作家其实承担不了创世主的角色，也别自我膨胀为基督，弄得自己精神错乱变成狂人，也把现世变成幻觉，身外全成了炼狱，自然活不下去的。他人固然是地狱，这自我如果失控，何尝不也如此？弄得自己为未来当了祭品且不说，也要别人跟著牺牲。&nbsp;&nbsp;<br />
<br />
<br />
　　这二十世纪的历史不必匆匆去作结论，倘若还陷入在某种意识形态的框架的废墟里，这历史也是白写的，后人自会修正。&nbsp;&nbsp;<br />
<br />
　　作家也不是预言家，要紧的是活在当下，解除骗局，丢掉妄想，看清此时此刻，同时也审视自我。自我也一片混沌，在质疑这世界与他人的同时，不妨也回顾自己。灾难和压迫固然通常来自身外，而人自己的怯懦与慌乱也会加深痛苦，并给他人造成不幸。&nbsp;&nbsp;<br />
<br />
<br />
　　人类的行为如此费解，人对自身的认知尚难得清明，文学则不过是人对自身的观注，观审其时，多少萌发出一缕照亮自身的意识。&nbsp;&nbsp;<br />
<br />
　　文学并不旨在颠覆，而贵在发现和揭示鲜为人知或知之不多，或以为知道而其实不甚了了的这人世的真相。真实恐怕是文学颠扑不破的最基本的品格。&nbsp;&nbsp;<br />
<br />
　　这新世纪业已来临，新不新先不去说，文学革命和革命文学随同意识形态的崩溃大抵该结束了。笼罩了一个多世纪的社会乌托邦的幻影已烟消云散，文学摆脱掉这样或那样的主义的束缚之后，还得回到人的生存困境上来，而人类生存的这基本困境并没有多大改变，也依然是文学永恒的主题。&nbsp;&nbsp;<br />
<br />
<br />
　　这是个没有预言没有许诺的时代，我以为这倒不坏。作家作为先知和裁判的角色也该结束了，上一个世纪那许许多多的预言都成了骗局。对未来与其再去制造新的迷信，不如拭目以待。作家也不如回到见证人的地位，尽可能呈现真实。<br />
<br />
这并非说要文学等同于纪实。要知道，实录证词提供的事实如此之少，并且往往掩盖住酿成事件的原因和动机。而文学触及到真实的时候，从人的内心到事件的过程都能揭示无遗，这便是文学拥有的力量，如果作家如此这般去展示人生存的真实状况而不胡编乱造的话。&nbsp;&nbsp;<br />
<br />
<br />
　　作家把握真实的洞察力决定作品品格的高低，这是文字游戏和写作技巧无法替代的。诚然，何谓真实也众说纷纭，而触及真实的方法也因人而异，但作家对人生的众生相是粉饰还是直陈无遗，却一眼便可看出。把真实与否变成对词义的思辨，不过是某种意识形态下的某种文学批评的事，这一类的原则和教条同文学创作并没有多大关系。&nbsp;&nbsp;<br />
<br />
<br />
　　对作家来说，面对真实与否，不仅仅是个创作方法的问题，同写作的态度也密切相关。笔下是否真实同时也意味下笔是否真诚，在这里，真实不仅仅是文学的价值判断，也同时具有伦理的涵义。作家并不承担道德教化的使命，既将大千世界各色人等悉尽展示，同时也将自我袒裎无遗，连人内心的隐秘也如是呈现，真实之于文学，对作家来说，几乎等同于伦理，而且是文学至高无上的伦理。&nbsp;&nbsp;<br />
<br />
<br />
　　那怕是文学的虚构，在写作态度严肃的作家手下，也照样以呈现人生的真实为前提，这也是古往今来那些不朽之作的生命力所在。正因为如此，希腊悲剧和莎士比亚永远也不会过时。&nbsp;&nbsp;<br />
<br />
<br />
　　文学并不只是对现实的摹写，它切入现实的表层，深深触及到现实的底蕴；它揭开假象，又高高凌驾于日常的表象之上，以宏观的视野来显示事态的来龙去脉。&nbsp;&nbsp;<br />
<br />
　　当然，文学也诉诸想像。然而，这种精神之旅并非胡说八道，脱离真实感受的想像，离开生活经验的根据去虚构，只能落得苍白无力。作者自己都不信服的作品也肯定打动不了读者。诚然，文学并非只诉诸日常生活的经验，作家也并不囿于亲身的经历，耳闻目睹以及在前人的文学作品中已经陈述过的，通过语言的载体也能化为自己的感受，这也是文学语言的魅力。&nbsp;&nbsp;<br />
<br />
<br />
　　如同咒语与祝福，语言拥有令人身心震荡的力量，语言的艺术便在于陈述者能把自己的感受传达给他人，而不仅仅是一种符号系统、一种语义建构，仅仅以语法结构而自行满足。如果忘了语言背后那说话的活人，对语义的演绎很容易变成智力游戏。&nbsp;&nbsp;<br />
<br />
<br />
　　语言不只是概念与观念的载体，同时还触动感觉和直觉，这也是符号和信息无法取代活人的言语的缘故。在说出的词语的背后，说话人的意愿与动机，声调与情绪，仅仅靠词义与修辞是无法尽言的。文学语言的涵义得由活人出声说出来才充分得以体现，因而也诉诸听觉，不只以作为思维的工具而自行完成。人之需要语言也不仅仅是传达意义，同时是对自身存在的倾听和确认。&nbsp;&nbsp;<br />
<br />
<br />
　　这里，不妨借用笛卡儿的话，对作家而言，也可以说：我表述故我在。而作家这我，可以是作家本人，或等同于叙述者，或变成书中的人物，既可以是他，也可以是你，这叙述者主体又一分为三。主语人称的确定是表达感知的起点，由此而形成不同的叙述方式。作家是在找寻他独特的叙述方式的过程中实现他的感知。&nbsp;&nbsp;<br />
<br />
<br />
　　我在小说中，以人称来取代通常的人物，又以我、你、他这样不同的人称来陈述或关注同一个主人公。而同一个人物用不同的人称来表述，造成的距离感也给演员的表演提供了更为广阔的内心的空间，我把不同人称的转换也引入到剧作法中。&nbsp;&nbsp;<br />
<br />
<br />
　　小说或戏剧作品都没有也不可能写完，轻而易举去宣布某种文学和艺术样式的死亡也是一种虚妄。&nbsp;&nbsp;<br />
<br />
　　与人类文明同时诞生的语言有如生命，如此奇妙，拥有的表现力也没有穷尽，作家的工作就在于发现并开拓这语言蕴藏的潜能。作家不是造物主，他既铲除不了这个世界，那怕这世界已如此陈旧。他也无力建立甚么新的理想的世界，那怕这现实世界如此怪诞而非人的智力可以理解，但他确实可以多多少少作出些新鲜的表述，在前人说过的地方还有可说的，或是在前人说完了的地方才开始说。&nbsp;&nbsp;<br />
对文学的颠覆是一种文学革命的空话。文学没有死亡，作家也是打不倒的。每一个作家在书架上都有他的位置，只要还有读者来阅读，他就活了。一个作家如果能在人类已如此丰盛的文学库存里留得下一本日后还可读的书该是莫大的慰藉。&nbsp;&nbsp;<br />
<br />
<br />
　　然而，文学，不论就作者的写作而言，还是就读者阅读而言，都□在此时此刻得以实现，并从中得趣。为未来写作如果不是故作姿态，也是自欺欺人。文学为的是生者，而且是对生者这当下的肯定。这永恒的当下，对个体生命的确认，才是文学之为文学而不可动摇的理由，如果要为这偌大的自在也寻求一个理由的话。&nbsp;&nbsp;<br />
<br />
<br />
　　不把写作作为谋生的手段的时候，或是写得得趣而忘了为甚么写作和为谁写作之时，这写作才变得充分必要，非写不可，文学便应运而生。文学如此非功利，正是文学的本性。文学写作变成一种职业是现代社会的分工并不美妙的结果，对作家来说，是个十足的苦果。&nbsp;&nbsp;<br />
<br />
<br />
　　尤其是现今面临的这时代，市场经济已无孔不入，书籍也成了商品。面对无边无际盲目的市场，别说孤零零一个作家，以往文学派别的结社和运动也无立足之地。作家要不屈从于市场的压力，不落到制作文化产品的起步以满足时兴的口味而写作的话，不得不自谋生路。文学并非是畅销书和排行榜，而影视传媒推崇的与其说是作家，不如说作的是广告。写作的自由既不是恩赐的，也买不来，而首先来自作家自己内心的需要。&nbsp;&nbsp;<br />
<br />
<br />
　　说佛在你心中，不如说自由在心中，就看你用不用。你如果拿自由去换取别的甚么，自由这鸟儿就飞了，这就是自由的代价。&nbsp;&nbsp;<br />
<br />
　　作家所以不计报酬还写自己要写的，不仅是对自身的肯定，自然也是对社会的某种挑战。但这种挑战不是故作姿态，作家不必自我膨胀为英雄或斗士，再说英雄或斗士所以奋斗不是为了一个伟大的事业，便是要建立一番功勋，那都是文学作品之外的事情。作家如果对社会也有所挑战，不过是一番言语，而且得寄托在他作品的人物和情境中，否则只能有损于文学。文学并非愤怒的呐喊，而且还不能把个人的愤慨变成控诉。作家个人的情感只有化解在作品中而成为文学，才经得起时间的损耗，长久活下去。&nbsp;&nbsp;<br />
<br />
<br />
　　因而，作家对社会的挑战不如说是作品在挑战。能经久不朽的作品当然是对作者所处的时代和社会一个有力的回答。其人其事的喧嚣已荡然无存，唯有这作品中的声音还呼之即出，只要有读者还读的话。&nbsp;&nbsp;<br />
<br />
<br />
　　诚然，这种挑战改变不了社会，只不过是个人企图超越社会生态的一般限定，作出的一个并不起眼的姿态，但毕竟是多多少少不寻常的姿态，这也是做人的一点骄傲。人类的历史如果只由那不可知的规律左右，盲目的潮流来来去去，而听不到个人有些异样的声音，不免令人悲哀。从这个意义上说，文学正是对历史的补充。历史那巨大的规律不由分说施加于人之时，人也得留下自己的声音。人类不只有历史，也还留下了文学，这也是虚枉的人却也还保留的一点必要的自信。&nbsp;&nbsp;<br />
<br />
<br />
　　尊敬的院士们，我感谢你们把诺贝尔这奖给了文学，给了不回避人类的苦难，不回避政治压迫而又不为政治效劳独立不移的文学。我感谢你们把这最有声誉的奖赏给了远离市场的炒作不受注意却值得一读的作品。同时，我也感谢瑞典文学院让我登上这举世注目的讲坛，听我这一席话，让一个脆弱的个人面对世界发出这一番通常未必能在公众传媒上听得到的微弱而不中听的声音。然而，我想，这大抵正是这诺贝尔文学奖的宗旨。谢谢诸位给我这样一个机会。</div>

							
							
							
							
