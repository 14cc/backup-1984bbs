　　 <br />
　　作者：何清涟 <br />
　　-------------------------------------------------------------------------------- <br />
　　【7月28日讯】 2006年，中国政府日益加紧控制媒体的种种举措引起了国际社会的关注。 <br />
　　从2005年年底开始，中国媒体接连受到政府整肃的消息在网路上广为流传，从《新京报》停刊开始，继之是《中国青年报 26;冰点》被封，《公益时报》总编换人，《深圳法制报》关张，《百姓》一度停刊。尽管事情发生的原因各不相同，但这一连串事件却组成了中国媒体受政府整肃的悲情画面。 <br />
　　 <br />
　　紧接着就是美国国会在人权组织的压力下，就帮助中国政府控制网路一事，两度传召微软等四大网路公司。尤其是7月5日中国全国人大通过《突发事件应对法（草案）》，用法律的形式限制媒体报导疫情、灾害等突发事件，对于违反政府规定擅自发布突发事件事态发展信息的媒体，草案规定将处以“5万元以上10万元以下的罚款”。如此公然限制新闻自由的恶法出台，终于将国际社会对中国政府的良好期望撕得粉碎，外国驻华媒体终于开始考虑他们的记者对中国的采访报导是否也会受到这条法律的限制－－而这距离我完成“中国政府如何控制媒体”这一研究报告已经有两年半之久。 <br />
　　 <br />
　　研究中国政府如何控制媒体的想法，并非始自我流寓海外。早在当年就职于深圳一家媒体时，因为对中国政府控制媒体的内幕有太多的了解，就一直在搜集资料，期望有一天能够将其写出来公诸于世。而来到美国之后，承蒙总部设在纽约的中国人权资助，使我在2003年完成了《中国政府如何控制媒体》这一研究报告。目前这本英文稿就是在当年这部研究报告的基础上增补修订完成的。 <br />
　　 <br />
　　通过这本书阐述的事实，读者会明白一个严酷的现实：在中国，是中国政府在监督媒体与媒体从业人员，而并非媒体这些所谓“社会公器”在发挥舆论监督作用－－而且中国媒体与民主国家媒体最大的不同，完全可由中国政府对媒体的定义而见一斑，中国的媒体号称“党的喉舌”。由于拙着对中国政府制度化控制传媒已有系统的阐述，在这篇为台湾读者写的序言中，笔者就只分析在写作这本书时，中国之外的媒体采访我时，经常会提出的一些带有普遍性的疑问－－因为这些疑问代表海外对中国传媒现状的一些普遍误解。 <br />
　　 <br />
　　误解之一：市场化能够促进媒体自由化 <br />
　　 <br />
　　当我申请“中国政府如何控制媒体”的研究资助时，反覆遇到的一个提问是：“中国的媒体正在市场化，外国传媒也在陆续进入中国市场，这些都将促使中国传媒业的自由化，这种情况下，中国政府还能够成功地控制媒体吗？”－－这个问题远未过时，就在2006年2月16日BBC英文部的Tim采访我时仍旧提出同样的问题。 <br />
　　 <br />
　　我只能尽量简明扼要地陈述事实。中国媒体业人士对市场化有一段精确的描述，那就是：在不放松新闻管制的情况下迫使媒体下海（即市场化），有如先捆住一个人的手脚，再一脚将这个人踢下海。在禁区多多、新闻来源相对单一的情况下，中国媒体为了在市场中立稳脚跟，只好被迫在声色犬马上做文章，中国网路媒体上的色情内容早已经远远超过了世界各国。而中国政府也乐意将国民引导到放纵物欲，不问政治的犬儒式生存之道上去。至于外国媒体在中国投资，也必须遵循中国的新闻审查规则，尽量不碰政治话题。 <br />
　　 <br />
　　而外国各大媒体在中国为了生存，为了在巨大的中国传媒市场当中分一杯羹－－虽然这仅仅只是想像，但已经让他们激动不已，怀抱这种理想的典型是默多克的新闻集团与英国的BBC。他们忙于向世界描绘的是这样一幅图景：中国传媒市场是一块巨大的蛋糕，如何瓜分这块蛋糕，将牵涉到21世纪世界媒体市场的利益格局。如果说世界各大传媒在进入中国市场初期，还曾经幻想过假以时日，缓慢地促进中国的新闻自由，但到了2000年以后，早已没有任何传媒集团愚蠢到要在中国坚持传媒理念。大家心照不宣的话题是：在绝对服从中国政府政治领导的前提下，中国当局究竟将中国媒体市场这块蛋糕赏给谁吃？ <br />
　　 <br />
　　对自己在中国市场上瓜分一块蛋糕最有信心的当属世界传媒大亨、新闻集团（News Corp.）的默多克。多年来，为了成为中国政府的好朋友，默多克曾花了不少精力金钱与中国高官结交，并获得相当大的成功。而他的成功秘籍就是从不涉足政治领域，更不关心中国的民主、自由与人权，甚至中国媒体都关注的民生也不在他的视野之内。 <br />
　　 <br />
　　对促使中国媒体自由化，他更是刻意表现出一副漠不关心之态。他甚至还采取了一些中国式的借道方式，比如投资香港凤凰卫视（这家电视有很明显的中国政府背景），希望通过这种方式进入中国传媒市场。总之，直到2005年8月3日中国文化部发布《关于加强文化产品进口管理的办法》之前，谁想要告诉世界：市场化并不能促进中国媒体自由化，谁都认为言说者有偏见。 BBC虽然还未走得那么远，但至少也效法默多克的态度。 <br />
　　 <br />
　　默多克在中国撞了墙之后，市场化将促进中国传媒自由化这一声音暂时沉寂下去，但将市场化与中国政治民主化挂起钩来的声音还照样那么强烈。这些人误解了一个基本事实：市场经济体制可以与多种政治体制相结合。比如历史上曾有纳粹的极权政治，现实中有美国的民主政治、新加坡的威权政治与拉美国家的军政府统治，这些国家都是高度奉行市场经济的国家。 <br />
　　 <br />
　　误解之二：网路能够促进中国的信息传播自由化 <br />
　　 <br />
　　20世纪留给人类最伟大的科技文明就是网路的发明与普及。毫无疑问，在若干年后，它必然对中国的政治民主与新闻自由产生极其深刻的影响。但如果将这个结论往近拉至现在及今后5－10年，这个结论就不能成立。因为网路在中国的使用，却让中国政府投入巨资发展了网路业的一大偏门：网路控制。 <br />
　　 <br />
　　作为一个网路的使用者来说，要想弄清楚中国政府如何控制网路并非易事。我在写作中国政府管制网路这一部分时，非常有幸地发现了几篇英文文章，其中对我帮助最大的那篇题为“金盾工程：中国庞大的电讯科技监控系统”。该文作者Greg Walton曾参加中国的互联网开通建设工作，对外国公司提供技术支持，帮助中国政府建设网路控制系统的情况非常了解。回到美国后良心发现，写了这么一篇文章。另一位作者干脆在他的文章里指出，美国有责任帮助中国人民夺回互联网这一促使信息自由流通的工具，“我们可以铺下革命的联络网路。如果我们不这么做，中国未来的世世代代将不会原谅我们。” <br />
　　 <br />
　　我根据自己对国内互联网的分析，以及这些英文文章对外国高科技公司内幕的介绍，写成了有关互联网控制的有关章节。此后，我多次在美国国会作证时提出要求：希望美国立法限制美国公司与中国政府之间进行这种不光彩的技术合作。 <br />
　　 <br />
　　在研究中国网路控制方面起了重要作用的有哈佛大学的一个项目，openNet Intiative。这个项目的研究小组有关中国的报告是《中国的网路过滤，2004－2005》，而事实上他们这个报告是连续性的，在此之前还有 2003年中国网路控制情况的一个报告。去年这个项目的主要参与者在国会的美国经济安全审查委员会作证时，也曾要求国会立法限制美国公司帮助中国控制网路言论。 <br />
　　 <br />
　　在这里还必须要提到的两个国际组织：总部设在巴黎的“记者无疆界”与总部设在美国纽约的“保护记者协会”。这两个组织从未相信过中国政府的谎言，一直在为促进中国的新闻自由而做持续不懈的努力。我自己当年遭受中国政府的迫害，被安全局特务24小时全天候监控时，记者无疆界组织为此专门给******写信，要求停止对我的迫害。其他受迫害的中国记者也都得到这些组织的关注。现在世界终于相信中国媒体严重受到控制，言论自由于中国人还只是个遥远的梦，这里面，有上述这些组织与研究者的功劳。 <br />
　　 <br />
　　误解之三：中国人可以骂国家领导人＝言论自由 <br />
　　 <br />
　　还有一些人士（包括常常回国的海外中国人）常说：中国人已经有了言论自由，因为他们可以骂国家领导人。有一位从事人权活动的女士曾充满怀疑地对我讲：她的家人告诉她，目前中国人在朋友家人之间，什么话都可以讲，没有什么禁区。末了她还问：“你的文章不也是在国内媒体上大量发表吗？” <br />
　　 <br />
　　对于我的著作与文章在国内发表之后的遭遇，我可以列举出若干媒体因发表我的文章而受到整肃的事例。但将朋友家人之间可以自由讨论等同于言论自由与新闻自由，却是个很大的误解。我不止一次地对持类似误解的人解释：私人之间的自由谈话与公共领域内的言论自由有实质区别。前者只表示在彼此信任的人之间一种意见交流，而后者才代表社会公共领域的言论自由。 <br />
　　 <br />
　　最重要的是，这一见解还忽视了一点，那就是在苏联斯大林铁腕统治时期，苏联还有一种“厨房文化”，即家人坐在一起讥骂苏联当局的种种倒行逆施。而中国人现在这种私下里可以批评时政，但在公共场合又被迫说假话的政治文化，只使中国人现在养成了一种两面性格，随时在不同的场合根据政治需要变换面孔。 <br />
　　 <br />
　　误解之四：中国老百姓不需要新闻自由 <br />
　　 <br />
　　由于中国的媒体实行批准登记制，任何媒体都必须挂靠在党政机关之下，让党政机关做主管与主办单位，更兼中国公众无结社自由，这就使得中国公众完全被剥夺了在公共领域内发言的任何可能性。这种现象外国人无从了解，而中国政府又常常代民立言，甚至声称中国老百姓素质太低，中国实行民主政治的条件还不太成熟，因此许多外国人常常问一个同样的问题：“中国老百姓是否觉得经济生活改善就已经足够，不需要言论自由与新闻自由？” <br />
　　 <br />
　　这实在是太深的一种误解。应该承认，******时代的极权政治与现在的新极权政治，当局者确实在利用权力剥夺人民的记忆并重构历史。在这样一个国度里，政府对人的思想的的控制和操纵不仅能扭曲国民的历史记忆，并且能够使人民对人权、自由、民主等普世价值产生免疫力与抵抗力。在这种情况下，要求九亿农民中的绝大多数阐述关于新闻自由的理念当然不太可能，但中国底层社会近十余年来几乎养成了一种将报社电视台等媒体当作申诉冤情之处，至少表明他们已经模糊地知道通过公众传播媒介帮助他们讨公道，申诉自己的利益主张。这种投诉大多数时候无果而终，但愤慨的民众却难免发出对传媒的指责，比如“你们也是吃官饭的，当然官官相护”之类。 <br />
　　 <br />
　　而中国知识界对新闻自由与政治民主化之间的因果关系更是心知肚明，这些年来一直有不少人身体力行，为了披露一些社会真相、说一些真话而被中国当局无情整肃。为了让人们了解中国记者与知识界当中的良知人士为了中国的言论自由与新闻自由所付出的巨大代价，我在研究报告当中专门撰写了“戴着镣铐跳舞的中国记者”与“牺牲者墓园”。 <br />
　　 <br />
　　中国的民主化必须由争取新闻自由开始 <br />
　　 <br />
　　平心而论，在九十年代中后期，中国曾出现过一段言论管制相对宽松时期。当时，《南方周末》的大胆敢言一度在报业独领风骚，而《书屋》等杂志更是一度成为知识界人士发表前沿思想的阵地。与这些媒体相比，《中国青年报》的“冰点”所载文章并不算出位。只是上述媒体相继遭到大整肃阉割之后，“冰点”以及后来开办的《新京报》就成了韭菜地里最高的那一茬韭菜。而这两家绝不涉足政治，只涉足社会领域的报纸最终也被中宣部挥刀相向，只说明中国政府对舆论的控制日益加紧，已经收缩到让人们无法再有任何腾挪余地。 <br />
　　 <br />
　　遭受整肃的媒体在中国几乎无时无之。以往媒体受到整肃时，知识界往往有人说这是因为所发文章言论太偏激，作者、编者不善于保护自己。这种心态说是“犬儒主义”也罢，说是“明哲保身”也罢，其结果使得中国政府日益肆无忌惮。 <br />
　　 <br />
　　最近这几年有一段话在中国被引用得最多，那就是“二战”以后，马丁 8226;尼莫拉（Martin Niemoller）牧师为了让人类社会永远记住纳粹屠杀犹太人这一血腥耻辱，在波士顿树起了一块纪念碑，碑上铭刻了这样一段话： <br />
　　“当初他们（指法西斯德国纳粹党）杀共产党，我没有作声，因为我不是共产党；后来他们杀犹太人，我没有作声，因为我不是犹太人；再接下来他们杀天主教徒，我仍然保持沉默，因为我不是天主教徒；最后，当他们开始对付我时，已经没有人为我讲话了……” <br />
　　 <br />
　　我想借此提醒我的中国同胞，争取自由民主是中国人自己的责任。但同时我还想说明，在这一过程当中，国际社会的帮助也绝不可少。一个极权专制的中国政府，对世界的自由和平只能起负面作用。也因此，国际社会负有促进中国民主化的义务。 <br />
　　 <br />
　　我很欣慰的是，从2006年开始，国际社会对中国人权状态与新闻自由的关注逐渐多了起来，一些重要的政治人物已经意识到一个专制极权的中国政府无法成为负责任的国际社会成员。这种关注使得为中国人权与政治民主化而奋斗的人不再那么孤独。而行动必始于思想，新闻自由不仅使人们获得更多的知情权，也将使诸多有价值的独立思考得以传播，而这些正是中国政治民主化必不可少的条件。 <br />
　　 <br />
　　 <br />
