　　 <br />
　　《雾锁中国－－中国大陆控制媒体大揭秘》一书，是何清涟在为中国人权撰写的研究报告《中国政府如何控制媒体》的基础上增修而成，全书25万字左右。由台湾黎明文化公司于2006年5月下旬出版，首印2000册已于7月初售完，第二次印刷的1000册亦已投放市场。 <br />
　　 <br />
　　作者秉承其一贯的研究风格，以非常详实的资料，系统分析了中国政府如何从制度与法律上对媒体进行严密的控制，“国家安全”这一名义是如何被中国当局用来惩罚那些不听命的记者以及对中国现状持批评态度的人士。该书还以大量篇幅介绍了中国政府在控制新兴传播媒介网路上的各种“努力”。作者以其在海外的观察便利，分析了外国传媒为了进入中国传媒市场而放弃新闻自由的理念，自觉与中国政府合作的各种姿态。有关中国政府迫害本国记者以及对海外华文媒体的渗透，书中亦有大量分析。 <br />
　　 <br />
　　有关中国传媒市场化的研究可谓汗牛充栋，但研究中国政府如何控制媒体，这本书堪称第一。作者本人有长期在中国媒体工作的经验，这本书打破了世人对中国传媒市场化必然促进媒体自由化的迷思，有助于人们了解中国政府控制媒体的严苛状态。可以说，本书为了解中国政治民主化为何困难重重提供了一把重要的钥匙。</div>

							
							
							
							
