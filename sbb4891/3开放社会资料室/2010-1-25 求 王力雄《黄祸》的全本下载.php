																											<div id="postmessage_452813" class="t_msgfont">王力雄（1953年5月2日－ ），中国大陆作家、藏学家、民族问题专家及民间环保人士。祖籍山东龙口，汉族，现居北京。<br />
<br />
王力雄生于吉林长春。母亲是长春电影制片厂的编剧。父亲王少林在文革前担任长春第一汽车厂的副厂长，1968年被打成“走资派”和“苏修特务”，死于被拘押中，定为“畏罪自杀”。1969年王力雄随从“牛棚”释放的母亲下乡插队4年，开始写诗。1973年作为工农兵学员在吉林工业大学汽车专业学习，大学期间（1975年）产生“逐层递选制”的设想。毕业后分配到长春第一汽车厂工作，先在车间当工人，一年后成为车间技术员，开始写小说和电影剧本。<br />
<br />
1978年王力雄主动申请调动到湖北第二汽车厂工作，从事计算机技术应用于企业管理，其间参与民主墙活动，并在油印刊物《今天》发表第一篇小说――《永动机患者》。1980年以“借调”形式离开二汽，脱离中国官方体制。<br />
<br />
此后王力雄写电影剧本，并在电影摄制组工作。1983年写作第一部长篇小说《天堂之门》。1984年，王力雄独自在青海藏区的黄河源头乘用汽车内胎扎捆的筏子漂流1200余公里，横贯黄河上游的藏族地区，从此开始对西藏的关注。1985年－1986年年写作纪实小说《漂流》，1987年出版。1988年加入中国作家协会。1988年开始写作政治寓言小说《黄祸》，署名为“保密”，1991年由香港明镜出版社出版。1991年－1994年，写作《溶解权力――逐层递选制》，1998年出版。<br />
<br />
1994年，与梁从诫、杨东平、梁晓燕等人成立了中国大陆第一个民间环保组织――自然之友，此后策划和亲自参与数个长期项目。<br />
<br />
1995年－1998年，在十次深入西藏和各省藏区，在藏时间累计2年，走遍所有藏区的基础上，写作《天葬：西藏的命运》。此后王力雄四次与第十四世达赖喇嘛见面交流。1999年1月起，王力雄在新疆收集资料，准备写新疆民族问题的著作。1月29日，被中国国安部以涉嫌泄漏国家机密为由逮捕，关押42天后释放。王力雄以此经历写成《新疆追记》一文，在网上公开发表。1999年出版文集《自由人心路》。<br />
<br />
2001年5月2日，公开发表声明退出中国作家协会，他在声明中发问：“究竟是中国的作家天生就是僵尸，还是中国的“作家协会”想把并且正在把中国的作家变成僵尸？”<br />
<br />
2002年4月，出版了《与达赖喇嘛对话》。2002年创办“递进民主”个人网站。2002年12月，发起对四川藏区阿安扎西活佛案的签名请愿活动，呼吁当局公正审理。2003年2月，因政府方面的命令，时任“自然之友”理事的王力雄被自然之友除名。2004年，由台湾大块文化出版《递进民主――中国的第三条道路》。2004年底，与藏族女作家唯色结婚。<br />
<br />
2007年10月，经过历时九年的调查和思考，在台湾大块文化出版新疆问题著作《我的西域，你的东土》。<br />
<br />
主要著作<br />
<br />
* 《黄祸》，1991年，政治寓言小说。该书描绘中国陷于政治、经济、文化、人口与生态的重大危机，终于导致整个社会的总崩溃，难民大批冲出国境，危及世界和人类的存在。这部小说引起海外媒体追踪报道，王力雄也由此获誉“中国最敢言的作家”。该书后来入选亚洲周刊二十世纪中文小说一百强，列第41位，至今仍在港台及海外畅销。<br />
* 《天葬：西藏的命运》，1998年，西藏问题专著。王力雄在书中以大量的亲身经历和实地所见来陈述他的论证，涉及到了西藏问题的历史、现状与各个方面。这部著作在汉人与藏人中都得到了高度的评价，被认为是迄今为止研究西藏问题最为客观、深刻的著作之一，展现出王对西藏这片土地的深切悯怀与过人洞见。海外媒体曾组织研讨会专门予以讨论。2009年推出增订版。<br />
* 《自由人心路》，1999年，以生态环境保护为主的思想随笔。书后附录小册子《溶解权力》，政治制度设计专著。这部书讨论的是选举方式，并提出了一个全新的概念－“逐层递选制”，就是希望以此方法使中国大陆的体制安全转型。<br />
* 《与达赖喇嘛对话》，2002年，对话录。<br />
* 《递进民主――中国的第三条道路》，2004年，政治制度设计专著。参考了主流学术界的行文方式，重新诠释了“逐层递选制”，并对中国当前的经济、生态、社会、政治危机进行了深入剖析。<br />
* 2007年7月，与妻子唯色合著的英文文集《Unlocking Tibet》在瑞士出版。书中收录他数年来研究西藏问题的多篇文章。<br />
