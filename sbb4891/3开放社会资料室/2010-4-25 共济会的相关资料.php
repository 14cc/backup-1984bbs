第三章&nbsp;&nbsp;共济会与法兰西革命<br />
<br />
&nbsp; &nbsp; 共济会从一成立，就注定了要与基督教为敌，他们不仅挑战了旧的教会体制，更是影响了整个西方的历史进程。在共济会成立的半个世纪以后，世界因此发生了如此多的剧变：一场文化思潮冲击社会，一场大革命将会爆发、一个新国家将会建立，一个新的世纪将会开始，西方在这个世纪中远远把东方文明抛在脑后，在这些剧变后面，我们仿佛都能看到共济会的大师们神秘的微笑。<br />
<br />
&nbsp; &nbsp; 1717年的共济会的开明吸引了当时很多的社会精英，共济会的教义也深深地影响着这些人。一种新的思潮在法国哲学家们中间逐渐兴起，他们尊重理性、尊重科学和人性，反对宗教独裁。受17世纪的科学大发现的鼓舞，他们认为宇宙是可以被感知和理解，是自然而不是超自然的――连天体运行这么神圣的事情都能被牛顿力学的雄辩精确的预测，还有什么神迹是不能理解的？他们认为，通过严格的使用“科学方法”可以解决每一个领域的问题。这场从英国兴起，在法国达到高潮的运动，为整个西方世界后来近三百年的进步打好了基础。这场运动现代人称之为“The Age Of Enlightenment”启蒙运动，照亮了中世纪漫长的黑暗年代Dark Age。<br />
<br />
&nbsp; &nbsp; 这些启蒙运动的哲学家们并非想象中大都是无神论者，他们大多数保持着自然神论的宗教观，认为上帝精密的设定了世界，然后其自己按照规律运行。咕咕还知道，他们公开的反对宗教独裁，支持宗教自由，这些和共济会的教义，息息相关，甚至完全有理由相信，是共济会员们影响和发动了启蒙运动。<br />
<br />
&nbsp; &nbsp; 我们在英语中间总是把“光明”和“感知”联系到一起，是因为我们平时接收到的信息有80％都是通过视觉。其实“science科学”来自于词根“sci = see“，就是“看的过程，感知的过程”，如果什么都能“感知”到，就叫做“consciousness意识”，而如果连道德上的所有都能被感知，那就是 “conscience良知”了。光的单位是“lumen”流明，而illuminate= il +lumen + ate-=使更亮”，就是“照明、启发”的意思。如果我们说放出光芒的人，就是“luminary天体、智慧的人”。<br />
