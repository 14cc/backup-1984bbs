																											<div id="postmessage_582657" class="t_msgfont">第一章&nbsp;&nbsp;共济会的传说<br />
<br />
&nbsp; &nbsp; 共济会Freemasonry，字面之意为&quot;自由石工&quot;（Free-Mason），全称为&quot;Free and Accepted Masons&quot;，其起源目前并没有确定的说法。根据其公式文献《共济会宪章》（THE CONSTITUTIONS OF THE FREE-MASONS，传说1701年写成，初版出于1723年）第一部《历史篇》的解释，共济会起源于公元前4000年－这一年称为A.L. （Anno Lucis，&quot;光明之年&quot;），他们自称为该隐的后人，通晓天地自然以及宇宙的奥秘。在共济会传说中如此记载：<br />
<br />
&nbsp; &nbsp; &quot;……这伟大的学问究竟从何而来？听吧，如同《创世记》第四章中记载的那样，早在大洪水之前有一个名叫拉麦的人，他是该隐的后代。拉麦有两个妻子，一个叫亚大，一个叫洗拉。亚大生了两个男孩，他们是雅八和犹八，奇拉生了一个男孩和一个女孩，他们是土巴该隐和拿玛。这四个人象征着人类对自然科学的探索：雅八是第一个研究&quot;地理&quot;的人类，他将家养山羊和野羊群分并且第一次使用石材和木材建造房屋；犹八是世界上第一个音乐家；土巴该隐发现了冶炼钢铁的技术；最小的妹妹拿玛发明了纺织技术……&quot;<br />
<br />
&nbsp; &nbsp; 这一段记述和《创世记》中并无二致，然而共济会的故事还有后话：<br />
<br />
&nbsp; &nbsp; &quot;……四兄妹知道自己对自然的探索会引起神的震怒，宇宙的伟大建筑者必将以水火惩罚窥视神之秘密的人类。在洪水来临之际，他们为了让伟大的学问流传人间，特地将知识镌刻在两根石柱之上，其中一根称为'埃布尔'－它不会因火而毁坏，另一根称为'拉特拉斯'－它不会毁于水中。其中一根石柱终于被一个人发现，他是三重伟大的赫尔墨斯（Hermes Mercurius Trismegistus），将伟大的学问的一小部分传授给人类。（赫尔墨斯为埃及神明透特的希腊名，其人在传说中为自然科学和自然哲学的大宗师、现实身份和神话中的身份已经不可分，通常被作为自然科学的一种拟人化）……&quot;<br />
<br />
&nbsp; &nbsp; 接下来便是古代共济会的登场，这部分记载开头引用《创世记》第10章和第11章：<br />
<br />
&nbsp; &nbsp; 洪水消退之后，神因挪亚的虔诚而喜悦，并且决定不再毁灭人类。然而人类并没有忘记对神之领域（自然科学）的探索，在复兴之后，大多数人类仍旧十分愚昧，只有石工（Mason）仍旧掌握着自然科学和几何学的秘密，根据这些知识他们知晓了人只不过是神的&quot;不完善的复制品&quot;。石工们发现如果通过自身努力，就可以克服人类自身的精神和肉体上的缺陷，从而回归神的领域。<br />
<br />
&nbsp; &nbsp; 挪亚的不孝之子含有一个儿子叫古实，古实有一个儿子叫宁录（Nimrod，传说中最强大的猎人），宁录是巴比伦的国王。当时石工们从四面八方聚集到巴比伦，开始建造一座通天塔，也就是传说中的巴别塔（Turris Babel）。宇宙的伟大建筑者这一次采用了一种幽默的手法进行惩罚，搅乱了他们的语言，于是他们荒废了造塔的工程而散布到世界各地……<br />
<br />
&nbsp; &nbsp; 而这些石工们从此不再将伟大的学问透露出去，他们组成秘密结社，采用口令暗号和秘密的握手方式表示身份、同时区分在团体中的级别和工作中的职务。这些&quot;自由石工&quot;在耶路撒冷建造了所罗门王的神殿，他们在古希腊被称为丢尼修建筑团，他们在中世纪为基督教徒建造教堂和各种大型石造建筑。石工们严守组织秘密，在建筑工地旁开设的集会所（Lodge）进行聚会，交流知识，他们信奉宇宙的伟大建筑者，通晓宇宙天文、人体解剖学、几何学的浩瀚知识，他们互相称为&quot;兄弟&quot;，奉行兄弟友爱、同舟共济。<br />
<br />
&nbsp; &nbsp; 共济会的始祖为希兰，他是建造耶路撒冷神殿的重要石匠之一，因被三个妒忌他地位及技能的上司所杀，而埋于殿内的青铜墓里，但不久即将再度复活。从此以后，凡是新加入共济会者，都要举行一场象征希兰死亡及复活的仪式。<br />
<br />
&nbsp; &nbsp; 1738年出版的《完全的共济会概况》中的版画《石工作业》，描绘了建设古代神殿的石工，左下角折断的柱子象征着共济会传说中希兰之死（栋梁的折断，象征着&quot;哲学的死亡&quot;和&quot;理性的新生&quot;），太阳和月亮代表所罗门神殿的两根铜柱，象征世界和人的二重性。<br />
<br />
&nbsp; &nbsp; 共济会的理神论明显继承了诺斯提教派的宗教思想（Gnosticism，基督教公开时期和基督教进行斗争的各种希腊化教派的总称，公元1世纪兴盛，4世纪因基督教压力而崩溃，剩余势力成为秘密宗教）。在隐蔽了将近1300年之后，天主教会再次面临强大的挑战，这令教廷十分恐惧。1738年，教皇克莱门斯 12世命令禁止天主教徒加入共济会，违反者将被教会开除（共济会允许天主教徒入会，但并不积极鼓励），在18世纪后半叶教廷一直致力于打压共济会，至今双方的关系仍不怎么样。而除了诺斯提思想之外，共济会也包含了13世纪左右兴起的喀巴拉（犹太教隐秘哲学）、拉丁炼金术的诸多元素。<br />
