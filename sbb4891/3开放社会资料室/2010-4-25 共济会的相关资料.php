《共济会宪章》（1723年）<br />
<br />
&nbsp; &nbsp; 初期共济会名义上对志愿者不作限制，人人平等，但是当时只有白人贵族和上层资产阶级可以入会，为了争取更大的影响力，共济会不断地招收贵族入会并且希望有朝一日能够得到王室的垂青。从第二代总导师蒙塔魁公爵（1721年就任）开始，大贵族在共济会中占有一段时间的支配地位。1722年沃顿公爵成为新一代总导师，继续奉行上层路线的政策。15年后，等待终于有了结果：1737年3月5日，乔治二世的长子－皇储弗雷德里克・刘易斯在伦敦的&quot;临时会所&quot;加入共济会。次年，安德森修改了《共济会宪章》并且将新版宪章献给皇储。<br />
<br />
&nbsp; &nbsp; 虽然这位皇储死的太早，没有坐上王位，但是其长子也就是乔治三世维护共济会的政治地位，这之后王室和共济会保持着良好的关系，乔治四世、乔治六世、爱德华七世、爱德华八世都是共济会会员。<br />
<br />
&nbsp; &nbsp; 共济会越来越多地参与到西欧社会的政治和思想活动中，虽然初期共济会仍旧奉行严守内部秘密的规定，但是1745年在阿姆斯特丹匿名出版的一本书籍完全暴露了共济会内部的规定、活动内容甚至入会礼仪等细节。然而共济会也因此将活动更加公开化，会员并不隐瞒自己的身份，他们也更公开地进行慈善活动，会员的阶层也逐渐向中产阶级转化。现代的共济会除了内部各级别的接头暗语仍旧保密之外，其余活动基本全部公开。<br />
<br />
&nbsp; &nbsp; 英国共济会于1753年分裂为古典派和近代派，双方长期对立，直到1813年才告和解，在19世纪中叶之后英国的共济会迅速丧失其社会改革的锐意和先驱性。而共济会给英国带来的另一大负面影响则是丧失了肥沃的北美殖民地，北美独立运动的先驱者几乎全部都是共济会会员。<br />
<br />
&nbsp; &nbsp; 共济会并非宗教，在成立的初期属于一种秘密结社，允许持有各种宗教信仰的没有残疾的成年男子加入，但志愿者必须是有神论者（可以是犹太教徒、基督教徒、印度教徒等）。近代共济会对于神的解释来自柏拉图的《理想国》中对造物主的阐述，他们认为神是一位理性的工匠（理性的人格化），而宇宙是神－&quot;宇宙的伟大建筑者&quot;创作的手工品，宇宙的秩序（作品的外形）来自神赋予的理性，这个外在的宇宙称为大宇宙（Macrocosm）；而每一个人类都是宇宙的影子也就是神的复制品&quot;小宇宙&quot;（Microcosm），然而由于材料的先天性的缺陷，这个复制品总是不完美的。然而如果人能够以理性为准绳，以道德为工具，不断地修正自身精神上的缺陷，那么最终人能够凭借自己的努力完善自身，也就是完成了&quot;内在神殿&quot;的建造，成为完美的&quot;石工导师&quot;并且进入神的领域。共济会会员（&quot; 石工&quot;）建设&quot;所罗门神殿&quot;的过程象征着人追求理性和自身完善的过程，在当时的社会背景下是一种先进的思想观念。通过奉行理神论的理想，共济会发起了启蒙运动并且在不到50年时间里迅速扩散到西欧、中欧和北美，建立起可以和天主教会匹敌的巨大体系。孟德斯鸠、歌德、海登、萨德侯爵、莫扎特、菲特烈大帝、华盛顿、富兰克林、马克・吐温、柯南道尔、加里波第……无数共济会会员的名字如同星光一般闪耀在西方近代史的夜幕之中。<br />
