<br />
第二章&nbsp;&nbsp;共济会的兴起<br />
<br />
&nbsp; &nbsp; 而共济会褪去传说的迷雾，正式出现的最早记载始于1717年的英国。1716年之前，伦敦的四个小酒馆中聚集着贵族和高级神职人员，举办类似高级俱乐部的聚会，内容多半是社交活动、娱乐和饮食为主，这些人是近代共济会的真正创立者。1717年6月24日&quot;圣约翰日&quot;（圣约翰是共济会古典派的守护圣人），四个会所（Lodge）的共济会会员（Mason）联合成立了第一个总会所（Grand Lodge），会员投票选举安松・塞亚为第一代总导师（Grand Master）这一天也就是近代共济会运动的开端。而这一时期也正值传奇的蔷薇十字运动的影响消退之际，近代共济会摆脱了石工团体的&quot;实践性的石工&quot;性质，成为&quot;思想性的石工&quot;、亦即投身社会改革的政治团体。<br />
<br />
&nbsp; &nbsp; 当时共济会在英格兰和苏格兰一共只有约700名会员，很快更多的志愿者希望入会，原来的那种沙龙性质的聚会已经不能管理如此之多的新会员，为了制订内部的条约规则，共济会于1721年开始筹备编纂自己的宪章。总会所委派新教长老派牧师詹姆斯・安德森完成这项工作，安德森根据共济会的古代条文《传统宪章》进行编纂，在1723年（共济会历5723年）出版了《共济会宪章》，这部宪章分为历史、责任义务、通则三个部分。<br />
<br />
