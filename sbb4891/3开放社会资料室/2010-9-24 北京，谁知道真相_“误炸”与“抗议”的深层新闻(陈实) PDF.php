书评《北京，谁知道真相》(舒崇)<br />
原帖：<a href="http://beijingspring.com/bj2/2000/380/2003817212917.htm" target="_blank">http://beijingspring.com/bj2/2000/380/2003817212917.htm</a><br />
作者：舒崇<br />
<br />
<br />
去年“五八”使馆被炸事件已经过去一年多了，当年喧嚣一时的反美示威抗议活动也早已平息下去了，可是，有关这次事件的前因后果、来龙去脉，至今仍留下若干疑团。今年二月，香港的太平洋世纪出版社出版了《北京，谁知道真相?》一书，该书的副标题是“‘误炸’与‘抗议’的深层新闻”。作者署名“陈实”(看来是个化名)，北京某报高级记者。全书共314页，前人民日报评论部主任编辑吴国光博士为这本书写了一篇序。正象吴国光写到的那样，围绕着使馆被炸事件之后北京出现的种种问题，这本书都提供了详尽和真实的答案。俗话说:“外行看热闹，内行看门道”。陈实的这本《北京，谁知道真相?》是一本既能看热闹，更能看门道的好书。<br />
<br />
《北京，谁知道真相》不是一本理论书，而是一部长篇现场报告。作者由于工作关系，有机会经常出席包括中宣部“吹风会”在内的高层会议，因此能够了解许多外人难以了解的“内部消息”，又称得上北京的地头蛇，方方面面的关系象蜘蛛网一样稠密，对社会底层的动态也十分熟悉，再加上沉迷于互联网，对海外的信息也相当灵通。笔锋所及，包括了街头活动、校园动向、媒体内部运作以及高层若干内幕。<br />
<br />
通过这本书，我们可以了解到在事件发生之初，中共当局的紧张和困窘，一方面想利用部分民众的反美情绪转移视线，渡过“六四”十周年这一关，另一方面又唯恐民众的抗议活动失去控制，并且担心中美关系破裂，在国际上陷入困境。通过这本书我们还可以了解到，原来这次反美示威抗议活动，在很大程度上其实是当局暗中鼓励、操纵和组织的产物，而就在自发参加抗议的民众中间，也不乏借题发挥的成分。<br />
<br />
作者告诉我们，第一批到美国大使馆示威的人被打了，有人说是被使馆门口等签证的人给打了，但其实是给使馆门口的警察打的，打人的是新派去的便衣警察，本来负责四五六月份敏感期使馆区的治安，当时还没接到可以游行的通知，一见有人来游行，先打一顿再说。作者还写到一件小事，北京某大学组织学生抗议示威，有个年级共一百多人，上面要求有一半的人参加游行，结果报名的只有两个，最后系里以“表现记入档案”相要挟才凑够了人数。游行的人知道有组织撑腰，有点过激行为、做点出格的事，不但不会惹麻烦，还会得到某种鼓励。<br />
<br />
不奇怪吗?大使馆被炸，炸死的却全是记者。我们知道，在使馆事件中被炸死的三个记者――许杏虎、朱颖、邵云环――都是光明日报国际部的驻外记者，有传言说这三名记者实际上是中共安全部人员。作者告诉我们，光明日报驻外记者都属于国际部，而国际部的人在报社是非常特殊的，显然和其他记者不是一类。国家安全部以某个新闻单位记者的名义外派人员，在新闻界早就是公开的秘密。作者还提到，许杏虎写的战地日记，因为写的比较客观，领导批评是“屁股坐到北约那边去了”扣住没发表。<br />
<br />
作者写到了由中共一手控制的媒体在五八事件中是如何运作的，扮演了何种角色，也写到了媒体的工作人员在私下交谈时表达的不同意见以及对当局的冷嘲热讽。尽管在五八事件中，有少数知识分子按照当局的鼓点起舞，但多数知识分子扮演了“沉默的一群”，还有一些人勇敢地发出自己独立的声音，这在当时的气氛下尤其难能可贵。事实上，《北京，谁知道真相》这本书的写作与出版，本身就代表了中国知识分子勇气和良知。它为这一段历史留下了珍贵的证词。<br />
<br />
在“五八”事件一年多以后再读这本现场实录，读到某些知识分子当时夸张幼稚的表演，真为他们脸红。再看一看中共对美国外硬内软的态度，不是很令人深思吗?<br />
<br />
《北京，谁知道真相》一书材料丰富，文笔生动，很有可读性。作者用事实和形象说话，而不是把自己的观点强加于人。不论你对“五八”事件原先持什么观点，我相信你都会对这本书发生兴趣，都能从书中得到更多的信息，并由此引出新的思考。<br />
<br />
<br />
（原载《北京之春》2000年9月号）<br />
