在为数众多的中国人虐杀日本人的事件中，没有能比得上通州事件的。<br />
如此残忍的暴行，就是连恶魔都不会做的极恶毒的行为。<br />
目前我国流行的说法是，当时通州的冀东政府保安队的兵营被遭到日本轰炸机的轰炸，导致他们疑心重重，从而叛乱。<br />
也就是这个事件最直接的责任方是日本。<br />
但是，日军误炸这个说法是过去的说法，连中国都不承认。<br />
误炸是在昭和12年（1937）7月27日，得知消息后，细木特务机关长直接前往冀东政府的首脑殷汝耕长官那里致歉，并亲自前往现场视察，慰问死者的家属。而且，次日28日，细木召集了保安队教导总队的干部，解释了误炸的原因，并表示一定会努力处理好善后事项。“误炸原因说”这种说法不能解释为什么保安队会叛乱和对日本人的大屠杀，这是通州事件中不能解释的一个疑点。<br />
但是近来，来自中国方面的资料证明，通州事件是冀东保安队第1、第2总队事前计划好的行动。<br />
例如，张庆余（当时冀东保安队第1总队长）著《冀东保安队通县决起始末记》（原国民党将领抗日战争体验记从书〈七七事变〉所载）和《战火蔓延，平津陷落》及《关于冀东保安队决起》等。<br />
详细的内容就不冗述，仅用起主要内容。<br />
昭和10年（1935）11月冀东防共自治委员会成立后，河北保安队被改名为冀东保安队。12月，冀东政务委员会成立，宋哲元就任委员长后，张庆余、第2总队长张砚田和哥老会（明朝开始的秘密结社）头子张树声一起和宋哲元见面（张庆余、张砚田二人也皆为哥老会成员）。<br />
宋哲元先对两人的抗日决心表明 “代表政府”欢迎，其后要求各人要作好加强军事训练的准备工作，并每人各奖励1万元。<br />
2人在发誓“跟随委员长、精忠报国”后，宋哲元连说：“太好了，太好了”。<br />
通州反正这个事件确实和这次会面有关系-------这个张庆余自己都说了。<br />
第2年昭和11年（1936）春天，中共支部已经在张砚田的第2总队里有组织了。<br />
和宋哲元会面后，冀东保安队和29军（军长宋哲元）秘密达成了相互协作，昭和12年（1937）卢沟桥事件爆发后，张庆余等待河北省主席冯治安的指示（当时宋不在北京）。<br />
冯当时指示，为了配合和呼应29军的作战，保安队要在通州起兵，同时保安队对丰台从侧面与29军形成夹击。冯把命令传达给了29军参谋长张越亭。张直接把冀东保安队第1、第2总队编入作战序列。<br />
另一方面，张庆余、张砚田两个总队长和通州特务机关细木中佐在围绕通州防备29军进攻的军事会议上，两人密谋欺骗细木机关长，提出要把分散的保安队集结到通州以保护侨民的提案。<br />
信赖这两个人的细木中佐表示同意将保安队集结在通州保护通州各处的日本侨民。<br />
欺骗成功之后，决定利用7月28日夜12点通州城门关闭，一切交通通信中断的时机为掩护进行起兵。<br />
中国方面关于通州叛乱的最新资料就是上述，没有关于误炸的说法。<br />
通州事件其实是已经密谋了2年的对日本人的袭击事件，那种由于飞机误炸导致保安队生疑而起兵的说法是行不通的。<br />
就象前文所述，南京国民政府方面播放了“日军溃败”的谣言。<br />
假如被“击溃日军”的宋哲元的29军攻入冀东，那自己的命运会怎么样呢？<br />
这个时候，冀东政府可以说是十分危险的吧。<br />
如果能先发制人，生擒殷汝耕，把他交到宋哲元和蒋介石的手里，必定会受到重赏。<br />
这就是相信南京的谣言广播的士兵们的想法。<br />
于是，就趁对手兵力最薄弱的时候，对昨天还是友军的日本守备队进行攻击。<br />
