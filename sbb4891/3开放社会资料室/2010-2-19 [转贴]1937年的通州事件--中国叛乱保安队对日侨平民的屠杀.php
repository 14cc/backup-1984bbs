保安队制造谣言叛乱<br />
<br />
<br />
1937年7月29日，也就是卢沟桥事变爆发三周后，北平（北京）东面的通州爆发了大规模的屠杀日本人的事件。也就是所谓的“通州事件”。<br />
对于这场世纪大屠杀，不仅中国反面丝毫不提，就连我国的历史学者乃至教科书、报纸期刊也很少看到。<br />
怀着缅怀那次屠杀中被杀害的200多名同胞的冤魂的心情，我写下了这篇文章。<br />
昭和10年（1935年），亲日派政治家殷汝耕脱离南京国民政府，在冀东的首府通州市成立了冀东防共自治政府，并拥有了人数在1万多人的冀东保安队。<br />
所谓保安队，其实是基于昭和8年（1933）年的塘沽停战协议而设置的。保安队不仅仅配备了步枪和刺刀，更配备了机枪和野战炮，所以战斗能力要远远超过一般的保安队。<br />
卢沟桥事件爆发后，当时在通州的宝通寺驻扎着中国第29军的一个营。7月25、26日廊坊、广安门的两处战斗爆发后，位于通州的特务机关认为，必须面对保安寺中国军队驻军的问题了。<br />
这个中国29军的一个营，在日中关系恶化后，对于这只部队的动向尚不清楚，假如任其驻守在那里的话会对有危险。<br />
于是，廊坊战斗之后的26日，日本方面向驻扎在宝通寺的那个营传达了必须向北京撤退的最后通告。 但是，那支中国军队一直对通报一直没有回应，于是27日早晨，我军开始对宝通寺驻军进行攻击，到了正午时分，宝通寺驻军被击溃。<br />
但是在战斗中，前来支援的关东军轰炸机误炸了靠近宝通寺军营的冀东保安队干部训练所，导致发生了数名保安队员死亡的不幸事件。<br />
当时，保安队在我轰炸机对地攻击的时候，处于好奇，队员们一起都跑到训练所的操场上观看，对于我方轰炸机，当时弄不清楚冀察和冀东的分界线，也不知道保安训练所的位置，看到脚下哄哄嚷嚷的人群以为是29军就作出了投弹的误判。<br />
接到误炸的报告后，细木特务机关长直接前往冀东政府的首脑殷汝耕长官那里致歉，并亲自前往现场视察，慰问死者的家属。<br />
而且，次日28日，细木召集了保安队教导总队的干部，解释了误炸的原因，并表示一定会努力处理好善后事项。<br />
正好，在误炸发生的27日，以传播谣言著名的南京电台（中国国民政府的官方电台）对外播报了“卢沟桥29军大败日军，中国军队已经陆续夺回丰台和廊坊，中央政府将陆续向华北派遣野战军，歼灭日军指日可待。”的谣言新闻，而事实与其正好相反。<br />
南京政府最后叫嚣着“军事会议决定，蒋委员长命令附近的29军大举进攻冀东，血洗伪都通州，并血祭汉奸殷汝耕的决议”的决定。<br />
听了这个广播后，对于北京当时的实际情况并不了解的通州的中国保安队来说，无疑动摇了他们的心理和意志。<br />
当时通州保安队分为张庆余指挥的第1总队和张研田指挥的第2总队，两支队伍当时都很早就受到了中国的人民战线运动的影响。<br />
当保安队听到了“日军溃退”“国军冀东反攻”这些南京电台的广播后，他们也产生了抗日的想法。因为他们认为，与其跟着日本人，还不如掉转枪头跟着29军，这样才更有利。他们于是的出了这种错误的判断。<br />
当时，通州的日本军守备队的主力正在攻击南苑，留守部队为藤尾小队的40人，山田自动车中队的50人，连宪兵、兵站（从事补给）、兵器部等其他部门在内一共110人的薄弱兵力。<br />
张庆余、张研田率领的两支保安部队瞄准我方部队力量单薄的时候，开始对我军民进行袭击。<br />
保安队趁着夜色袭击了长官公署并绑架了殷汝耕，保安队的主力则攻击日本军守备队。<br />
我方虽然只有极少的守备部队，但是依然顽强抵抗着数以千百计的敌军。<br />
守备队除了轻机枪、步枪、手榴弹以外，没有任何的重武器。<br />
守备队冒着敌人的集中火力，死伤不断的情况下开始努力撤退，日本侨民的住处和特务机关也挺不住了。<br />
禽兽般的中国保安队对日本侨民的暴行，也在这个时候开始了。<br />
通州日本特务机关遭到了相当于一个中队的保安队的袭击，包括两名日本少年在内的机关人员在艰难的抵抗着数十倍的敌人，终于最后不支，全员壮烈玉碎，特务机关被全部消灭。<br />
<br />
<br />
禽兽不如、方式新奇的屠杀<br />
<br />
<br />
中国保安队不仅仅攻击了我守备队和特务机关，更对日本侨民进行一家不剩的袭击，大量的日本无辜侨民（多数是老弱妇孺）被抢劫、施暴、凌辱和杀戮，其残忍令人发指。<br />
中国士兵用其特有的猎奇般的杀人方式，光天化日之下各处地方成了杀人的刑场。<br />
其凄惨程度，远远超过了过去的南京、济南事件，简直就是令人发指的尼港事件的再现。<br />
在远东国际法庭审判时，外务省曾就通州事件提出证据和发表诉讼，但是被法庭的威伯庭长否决了。<br />
同时这些证据在法院上被当作中国方面的证据而证明所谓的南京大屠杀，这样的判决是有失公正的。<br />
下面就整理的目击屠杀现场的证人的宣誓口供，让我们来看看那个惨剧吧。<br />
当时，曾任天津步兵队长兼????驻屯步兵第2联队联队长，参加7月28日的南苑战斗后于30日下午急忙前往通州营救侨民的证人萱岛高，证言如下：<br />
“在旭轩（饭店）附近有7到8名年龄在17、8到40的妇女被轮奸，被枪杀的裸体暴露着阴部，其中还有4到5人阴部被刺刀刺入导致死亡。在商铺和官府衙门附近的日本男性尸体脖子上有勒过绳子的痕迹，喷出的鲜血溅在墙壁上，简直不能用言语来形容。”<br />
场景最残忍的是一个叫近水楼的旅馆了。下面是曾经担任通州救援的第2 步兵联队步兵队长代理的桂镇雄的供词：<br />
“在近水楼的入口处有一具尸体好象是女主人。脚朝门口，脸上盖着报纸，好象她经过了剧烈的反抗，衣服在她倒下前已经被扒光，全身暴露在外，现在还能回忆起她的身上被刺刀捅了4、5刀。阴部好象被刀剜掉，四周都是血迹。帐房和配膳房里可以看出杂乱的脚印，很清楚就能看到被掠夺过的痕迹。中间房间有四具象是中年妇女一样的日本妇女，全部是好象经过痛苦挣扎后死亡的。尸体是重叠在一起，只有一具身体向上露出局部。配膳房和帐房里躺着一男两女，有的伏在地上，有的面朝上。明显有打斗过的痕迹，男的眼球被剜了出来，上半身象蜂窝一样的。两个女的背上都有刺刀刺过的痕迹。还有当时一年前曾经去过的咖啡屋里，有被绳子勒死的光着身子的尸体。房子里面是日本人的家，孩子和双亲都被惨杀。孩子的指尖被全部剁下。南门附近的一家日本人商店，象主人一样的尸体躺在路上，肋骨露了出来，内脏散乱。”<br />
袭击近水楼的是身着黑衣服的武装学生和保安队。他们残忍的杀害几名中年妇女后，然后抢劫了剩下来的十几个男女店员和旅客，再用麻绳把那些人串着绑起来，拉到空地枪杀。<br />
关于那次残忍的屠杀的情况，当时没有一个人知道。但是有个日本人从枪杀现场侥幸生还，逃到北京，世界才知道这个消息。他就是当时住在近水楼的日本同盟通信特派员安藤利。<br />
安藤的手记《继对支回顾录》里，那次屠杀的体验被收录在书中。<br />
安藤的体验记《通州日本人大屠杀》登载在昭和30年8月的《文艺春秋》，之后转载到《文艺春秋看昭和史》第1卷（昭和63年1月）。<br />
作为????驻屯军第2联队小队长，7月30日随联队主力共赴救援的樱井文雄证言如下：<br />
“进入守备队的东门后，几乎每隔几间屋子就横躺着侨民被惨杀的尸体。大家都十分悲愤。一边喊着“有日本人吗？”一边每家每户的搜索。发现有鼻子被钢丝穿透的孩子，有一只手腕被切断的老太太，有肚子被刺刀捅过的孕妇，这些尸体被放置在垃圾箱里和战壕中。有个饭店里，一家人的头和两手都被砍下来。所有14、5岁以上的妇女都被强奸过，简直惨不忍睹。在那个饭店里，有7、8名妇女被强奸后全裸杀害，有的阴部被插着扫帚，有的嘴里被塞满沙土，有的腹部被从上往下剖开，看着都受不了。在靠近东门的一个水塘里，有一家6口人脖子被绳子绑着，两只手合拢着被8根钢丝穿过，然后6个人象佛珠一样用绳子串起来，他们的尸体被发现在那个水塘里，塘水都被染红了。”<br />
这些证言里描述的连恶魔都为之侧目的惨无人道的虐杀，真是恶魔禽兽都自叹不如的暴行。<br />
被证实的虐杀方式，当时在远东法庭上作为证言的有如下几种：砍头，剜眼珠，剖胸剖腹，内脏取出，用东西刺入阴部等????军队管用的对日本人猎奇般的虐杀方式，从日清战争开始以来就形成了。<br />
