　　新的国际地位必将带来不断增长的责任。而中国政府却违反了多项它曾签署的国际协议，没有向民众提供它所承诺的政治权利。中华人民共和国宪法第35条规定：“中华人民共和国公民有言论、出版、集会、结社、游行、示威的自由。”事实却证明这些自由受到了明显的限制。<br />
　　For over two decades, Liu Xiaobo has been a strong spokesman for the application of fundamental human rights also in China. He took part in the Tiananmen protests in 1989; he was a leading author behind Charter 08, the manifesto of such rights in China which was published on the 60th anniversary of the United Nations' Universal Declaration of Human Rights, the 10th of December 2008. The following year, Liu was sentenced to eleven years in prison and two years' deprivation of political rights for “inciting subversion of state power&quot;. Liu has consistently maintained that the sentence violates both China's own constitution and fundamental human rights.<br />
　　二十多年来，刘晓波一直是为中国人民争取基本人权的一位强有力的发言人。他曾参加1989年的天安门事件；并带头起草了《零八宪章》，这是一份关于中国政治权利的宣言，发表于2008年十月十日的联合国《世界人权宣言》六十周年纪念日。接下来一年，刘晓波因“煽动颠覆国家政权”被判处有期徒刑十一年，剥夺政治权利两年。刘晓波一直坚持此项判决违反了中国自己的宪法和《世界人权宣言》。<br />
