													<h2>重新评价蒋介石</h2>
						
						
																											<div id="postmessage_423203" class="t_msgfont">作者:林蔚&nbsp;&nbsp;翻译:田呈莲<br />
<br />
最好的一本蒋介石传记终于出炉。蒋介石是中国近代最重要的人物之一，同时也是一个被误解最深、讽刺最多的人。他通过1925-1929年的北伐战争统一了中国，之后十年，也被称为&quot;南京十年&quot;，是中国经济与制度大发展的十年，也是中国最为自由的十年，而这一切却被1937年的日军侵华战争所打断。为了反击日军，蒋介石政府进行了顽强抵抗，而这一抵抗在一些人看来是中国20世纪最为辉煌的时刻。抗日战争结束后，蒋介石冒险发动了试图消灭对手共产党的战争，结果一败涂地。他撤退到台湾，在那里推行铁腕统治，直到1975年去世，享年87岁。<br />
<br />
蒋的中国追随者对他极其忠诚，而他在西方也不乏朋友--其中包括美国《时代周刊》的发行人亨利・卢斯，但是无论是生前还是死后，对于他的评价总是反面居多。只要中国共产党的胜利被看做是解决中国弊病的唯一途径--而这正是从20世纪30年代到70年代中期那些在政治、媒体以及学术上占主导地位的人所持有的观点-蒋的形象就是反面的。他反共，通过操纵权力妨碍那些能够更好利用它的人，他自以为有更好的办法重建中国，直到1950年，一直把更有办法的共产党人堵在建国大业的门外。<br />
<br />
现在全世界都看到了共产主义的真正代价，蒋和他曾经统治过的中国也越来越难被一笔勾销。在最近20年里，中国自身也正在对社会主义中国前的历史进行重新认识。比如，大家只要看一下庆祝中华人民共和国成立60周年的献礼片《建国大业》里所描绘的给人好感的蒋介石以及那些具有个人魅力的国民党军官和官员，就可以发现，这与之前中华人民共和国对他们所持的&quot;卑鄙小人&quot;的标准形象大相径庭。<br />
<br />
现供职于哈佛大学的陶涵（Jay Taylor）曾是美国国务院主管中国事务的外交官，已经出版了好几部著作，其中非常出色的一部就是关于蒋介石唯一的儿子蒋经国的传记。《蒋委员长》这本书（外国人对于蒋的&quot;委员长&quot;这一称呼众所周知，但是中国人却不怎么知晓）因为其规模庞大而取材众多。书的一部分素材来自于蒋自己的日记，这为该书提供了前所未有的准确的时间和事实框架。它还取材于苏联和共产国际的档案，这些资料使我们了解莫斯科对于蒋介石事业的兴衰是多么关键。此外，该书还参照了中国人和美国人的一些回忆录和访谈，这使得陶涵能够突破传统的历史学传记模式而提供一部更为可信的著作。<br />
<br />
从某种程度上将，对蒋介石的负面评价来源于一个人，那就是美国上将约瑟夫・W・史迪威，他当年被罗斯福派去给蒋做参谋总长，可是不久就开始鄙视他。这位外号&quot;醋坛子乔&quot;的史迪威把中国的领袖称作是&quot;微不足道的人&quot;（the peanut）,而且认为蒋是一个既缺乏智慧又没有能力的国家领导人，在抵抗日本方面也是一个失败主义者。陶涵在书中指出史迪威也是一个蹩脚的战略家。比如，现有的资料充分证明美国这位四星上将严重低估了在缅甸的日军的实力，由于他的错误判断，成千上万个士兵被派往雨林参加计划不周的密支那战役，而蒋所坚持的以防御为主的策略是显然更为谨慎，也会有取得更好的效果。但是，直到自己的晚年，（史迪威于1944年被召回，于1946年逝世），史迪威仍然对他的中国老战友恶语相向，毫不掩饰。比如，《纽约人》记者布鲁克斯・阿特金森战时对他的采访曾产生很大影响。他的抱怨也为另一部影响深远的著作--芭芭拉・塔奇曼所著、于1970年出版的《史迪威与美国在中国的经验》--提供了素材。<br />
<br />
另外一个反蒋的根源是由乔治・C・马歇尔将军领导的美国斡战后中国和平的失败。蒋的国民党政府和毛泽东的共产党政府不能组成联合政府的原因很多。陶涵指出，原因之一个是马歇尔对依赖美国政府提供援助的蒋介石有很大的影响力，而他对从莫斯科那里得到充分援助的共产党却束手无策。事实上，马歇尔从未充分认清形势，其他人也是同样。在中国以教育工作者身份呆了几十年并且讲一口流利汉语的美国大使司徒雷登认为中国共产党与莫斯科的联系是&quot;脆弱的和微不足道的。&quot;<br />
<br />
事实远非如此。中国20世纪的前期历史见证了俄罗斯和日本试图影响中国的博弈，它们都想通过资金、影响力、合作以及武力征服扩展各自在中国的利益。自30 年代起，日本的影响力是显而易见的。或许陶涵最大的贡献是使人们对苏联如何在关键时刻对蒋（和毛泽东）的事业起了决定性的作用有了一个清晰的认识。<br />
<br />
蒋一开始是作为孙中山的信徒而实现他的权力崛起的。孙中山在苏联政府的支持下创立了国民党。1923年，蒋介石在苏联花费了三个月的时间与共产国际的执行委员会进行磋商、寻求合作。1924年6月，当孙中山在黄埔军校的开学典礼上演讲时，蒋就站在他旁边。蒋后来成了黄埔军校的校长。黄埔军校是后来战无不胜的国民党军队的摇篮，而学校的成立依赖于苏联馈赠的270万元的开张费和每个月10万元的津贴，苏联同时还提供了武器。&quot;1924年10月7 日，第一批运送的8000支苏联步枪抵达，之后又运来了15000支枪、机关枪和大炮。&quot;1925年孙中山逝世后，黄埔军校的毕业生和苏联的资助成为蒋剿灭北洋军阀取胜的关键，并使蒋得以在20年代末在南京建立中华民国政府。<br />
<br />
然而，莫斯科不单单支持蒋。苏联人还给中国共产党提供帮助。斯大林意识到当时共产党力量薄弱，无法掌权，也不能够与让他担心的日本的军事武装抗衡。斯大林最初的想法是利用力量较小的共产党来影响甚至是控制相对庞大的国民党，或者是在共和国内部建立一个不大的&quot;红色中国&quot;。<br />
<br />
1933年6月2日，驻上海的共产国际代表提出了购买一架能够飞抵共产党根据地并提供补给飞机的计划，该架飞机将由一位美国飞行员驾驶。11月 2日，莫斯科指示上海要购买&quot;重型飞机、防毒面具和药品&quot;，并询问购买需要支付美元还是墨西哥银元。11月14日，上海共产国际代表报告已经收到了300 万墨西哥银元，并提出再要25万美元。<br />
<br />
苏联提供的资金和武器壮大了毛泽东的共产党的力量，就像苏联曾经资助过孙中山和蒋的国民党一样。在20世纪30年代，双方开始交恶，蒋针对存在农村建立了根据地并形成&quot;国中之国&quot;的的共产党发起了一系列围剿运动。1934年的最后一次围剿十分成功，共产党不得不突破国民党的防线，撤退到西北地区。如果当时共产党口袋里没有莫斯科提供的墨西哥银元并用其维持撤退中的军费支，并贿赂地方军阀&quot;高抬贵手&quot;，这次成功的撤离，也就是所谓的&quot;长征&quot;，根本是不可能的事情。当毛泽东的队伍最终到达位于西北边陲的宝安时，他告诉他的部下他的目标就是要不断扩大共产党的控制范围，实现与俄罗斯和蒙古的接壤。<br />
<br />
许多日本人都想要入侵苏联，但是斯大林知道如果牵制日本的话，他需要蒋和他的部队，这也就是1936年当蒋被绑架时他进行果断干预的原因。毛泽东知道并支持绑架蒋介石的计划，日本当时正在不断向华北推进。斯大林知道，如果没有蒋，中国将陷入群龙无首、无法抵制日本侵略的境地。毛泽东马上就听从了斯大林的意见，蒋介石获释。1937年全面抗战爆发后，斯大林通过新疆对中国军队提供陆路补给。到了南京战役时，涂有中国标志的苏联飞机以及苏联的飞行员都参与到了阻击日本人的战斗中。如果没有苏联的支持，蒋就难以维系。起初，他在苏联的支持下掌权；这时，在苏联的支持下，他挺过了生命中最危险的时刻。<br />
<br />
抗日战争结束后，斯大林重打算盘，因为日本人不再构成威胁。在战争即将结束时，苏联军队占领了东北。斯大林想把富饶又具有战略地位的中国东北地区占为己有，这是蒋绝对不会答应的。在中国的共产党现在可以象东欧的共产党一样协助把所在国变成苏联东德那样的苏联附庸国。苏联于是提供铁路和飞机运输协助把毛泽东的共产党军队输送到满洲去建造一个&quot;红色中国&quot;，同时拒绝和拖延蒋派军进入东北的要求。蒋急忙在外交上作出让步，希望至少能够与中共共同占领满洲。<br />
<br />
蒋以为他已经获得了苏联的默许，于是把最为精锐的部队投入试图把共产党赶出满洲的战役中，这是蒋一生中最大的败笔。尽管一开始他取得了重大胜利，但是正如陶涵在其书中所指出，这场战役迟早会以蒋的失利而告终。斯大林是不会允许蒋在那里取胜的。苏联的物质和后勤支持通过边境的铁路（所有的铁路线均在中共的控制之中）、空运甚至是通过北朝鲜的铁路源源不断地输送给中共的部队，仅从北朝鲜发运的物资车皮就有一千辆。<br />
<br />
在当时甚至在之后很长一段时间，人们对这些事情仍知之甚少。直到苏联的档案将莫斯科对中国共产党的大力支持公之于众之前，很多外国人都认为中国共产党的胜利是自给自足的，主要归因于贫苦农民的大力支持。社会理论家巴林顿・摩尔在1966年指出中国共产党的革命是&quot;一次农民的解放运动&quot;。但是事实并非如此。<br />
<br />
苏联最后一次进入中国历史是在20世纪的70年代初期，彼时距离蒋撤离到台湾已经很久了（陶涵对于这一时期有详细描述）。当时美国为了寻求与苏联的力量平衡，开始与中国政府进行沟通。通过引用美国政府的解密文件，陶涵对当时的绝密外交提供了一个有趣的注脚。华盛顿政府为了对当时的盟友蒋隐瞒其行为和目的大费周章，但他们不知道，中国的总理周恩来一直在向蒋提供中美接触的信息，他们彼此之间相互信任的关系可以追溯到内战时期。<br />
<br />
我不得不遗憾地说，这部由哈佛最富盛名的贝尔纳普出版社出版的著作也有一些缺憾。一个好的编辑是不应当允许这些错误出现的。书中有很多拼写错误，比如将苏联打败日军的关键性一战&quot;诺门坎战役&quot;的名字写错了，同样令人惋惜的是在拼音翻译方面出拼了一些错误。比如把台湾政务院总理郝伯村和驻美大使沈昌焕的名字也给写错了。<br />
<br />
但是这些非同小可的错误并不能使人们产生重要的误解。该书的主线没有因为冗长和繁琐的出版程序而消失。此书树立了一个新的基线，以后的历史学家再写蒋介石必须以此书为基准。陶涵的书是一个非常了不起的成就，是一部非常好看的书。如果我没有理解错的话，它也是一个从根本上改变对历史事件的理解方式的符号。<br />
