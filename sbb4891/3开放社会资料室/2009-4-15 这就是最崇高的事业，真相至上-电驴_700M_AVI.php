　　政治题材的批判力作<br />
<br />
　　在《暗朝汹涌》之后，《真相至上》是罗德・拉里又一部与政治有关的电影，这位常年热衷于政治题材的导演，对这部自编自导的新作相当满意：“《真相至上》是一部标准的惊悚作品，在整个电影策划之前，我看了西恩・潘与妮可・基德曼的《翻译风波》，那部电影启发了我，当中的许多内容都让我很赞叹，后来我在构思《真相至上》的时候，一直在思考这方面的问题。我并不想阐述一些太过阴暗的事情，或者让观众觉得我是利用电影发泄着对政府的不满，这些都不是我拍这部电影的主要原因。近几年我在媒体上看到了很多社会报道，有一些内容看上去虽然很简单，但实际情况想一下的话就会觉得非常玩味，你会去想：为什么事情会去这样做，结果会是这样？我整理了很多这方面的资料，然后从中挑选跟电影符合的内容，最后来进行拍摄。 ”<br />
<br />
　　谈及《真相至上》的主题，罗德・拉里说：“这部电影有一个很简单也很明了的主题，如同它的片名，一切以真相为主。电影从女主角瑞秋开始报道一个新闻的真相开始，最后以她揭露整个事情的真相结束，故事从头到尾都是有关于影片中各个人物对于‘真相’二字的看法和取舍。我想在现实生活中，每个人心里都有一种追寻真相的念头，但由于现实各种各样的压力，这种勇气可能无法贯彻下去。但在电影里，我想营造出一种比现实更大的压力，这样便让观众时时将自己代入主角的境地，然后问自己，如果我处在那种角度，我该怎么去做？这是我想和观众沟通的一点，我希望观众在看电影的时候也能感受到这一点。”<br />
<br />
　　缺一不可的演员<br />
<br />
　　《真相至上》网络了不少大牌明星，对于这些演员，罗德・拉里分别评价道：“影片的女主角瑞秋是由凯特・贝金赛尔扮演，整个故事都围绕着她，所以她的演出决定了这部电影的成败。凯特・贝金赛尔演得很出色，在拍摄的时候，我常常对她说，你现在是一个不顾一切追寻真相的记者，你面对的是整个社会、家庭和政府的压力，你要无时不刻的感受到这一点，所以片中的凯特・贝金赛尔显得非常的坚强，这还要得益于我们的化妆师和造型设计，凯特・贝金赛尔看上去就像是一个干练的女强人。马特・狄龙在片中的气质我很喜欢，他的角色很符合他在现实生活里的形象，而大卫・休默则比银幕上的他要风趣得多，我原来一直以为他是一个很闷很闷的人，结果在《真相至上》拍摄的过程中，我才知道我错了。这些演员都很棒，对于影片来说，他们缺一不可。”<br />
<br />
　　马特・狄龙坦言《真相至上》对他而言意义重大：“这部电影的剧本非常棒，而且很扣人心弦，虽然描写政治阴谋的电影很多，但《真相至上》有着他独特的魅力。我在片中扮演的是一位检察官，算是在影片里处处和凯特・贝金赛尔扮演的记者瑞秋作对的人，大家都认为我是反派，其实我并不这样看，我认为只是和女主角所处的立场不同而已。这也是这部电影有趣的地方，它提供了几种不同的视角，让观众思考，究竟谁对谁错。其实这不仅仅是电影，现实这样的情况也时有发生，大家可以想像一下自己身边的事，也许也存在着这样的双面性。”<br />
<br />
　　《真相至上》当中，凯特・贝金赛尔与大卫・休默扮演一对深爱对方的夫妇，对此，凯特・贝金赛尔笑道：“《六人行》对我的影响实在太深了，我在片场看到大卫，我都会叫他，嘿，Ross！他听到我这样子叫他就会显得很无奈。大卫・休默是一个很风趣的人，在《真相至上》里他则扮演一个性格比较沉稳的丈夫，影片一开始我们很恩爱，但随着剧情的发展我和他的感情也产生了裂痕，影片里有一场他和我吵架的情节，这段拍摄的时候他骂我骂得很凶，虽然我已经熟读了剧本，但拍摄完成的时候我还是觉得大卫・休默好像真的在生我的气，他演得真的是非常的棒。”<br />
<br />
　　而大卫・休默表示：“演女主角的丈夫并不难，因为本片中大部分时间镜头都给了女主角，我只要在旁边听女主角滔滔不绝的讲台词就可以了。这部电影让我和凯特・贝金赛尔成了好朋友，最开始是因为这个工作关系，但随后我发觉大家都挺谈得来的，我们也要经常交换对剧本的看法，所以很快大家都很熟了。导演对我这个角色的安排是比较内敛的，他认为比较像我的性格，但我后来对导演说，其实我不是那么闷的人，剧本后来也略微修改了一下，你会在银幕上看见发火的我。”<br />
<br />
