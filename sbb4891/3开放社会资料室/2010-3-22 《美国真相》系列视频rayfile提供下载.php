简介：本片记述了美国的商业医疗保险系统中的种种圈套，拥有医疗保险的美国公民在不知不觉中沦为医疗保险公司的人质，而5千万无力支付医疗保险的人只能悲惨地抱病终身。<br />
<br />
剧情简介 <br />
<br />
片中最先出场的是一个没有医疗保险的工人，意外的在工作中手指被截断，不得不在医院停供的选择：支付12000美元修复无名指或者近80000美元修复中指间做出选择。通过这一颇有荒诞意味的开片插曲，迈克・摩尔展开了他新片所要讲述的重点--美国的医疗保险系统中的种种弊端。虽然美国在医疗保障上的开销位居全球之首，但全美仍然有5千万人无力支付保险，而即便那些拥有医疗保险的美国公民，同样面对各种各样的规章麻烦与制度死循环，正常的治疗不能得到完全的保障。迈克・摩尔此番一个人担起导演，制片，编剧，演员等角色，以一种孤家寡人的姿态在继《华氏911》与《科伦拜恩的保龄》之后再次打造了这部批判现实、深刻揭露普遍社会问题的平民代言片。而这次所引起的社会震动却又远远超过了他此前的所有作品。 <br />
