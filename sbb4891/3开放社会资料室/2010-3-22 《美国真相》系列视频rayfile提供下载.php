《美国真相》之十：解构企业<br />
<br />
（The Corporation）<br />
<br />
<a href="http://www.tudou.com/programs/view/XM1HkVEjw7Q/" target="_blank">http://www.tudou.com/programs/view/XM1HkVEjw7Q/</a><br />
<br />
 <br />
<br />
简介：本片通过访问和拍摄揭示了当代的企业本质：今天全球最大的统治机构不是国家，而是跨国公司。 <br />
<br />
这部145分钟的纪录片耗时6年完成，访问近四十位教授、管理专家、以及企业总裁，例如九十六岁的彼得・德鲁克，前壳牌石油总裁史都华爵士，写成《企业的性格与命运》（The Corporation）一书。接着与两位电影制作人艾其博（Mark Achbar）和阿尔伯特（Jennifer Abbott）合作拍摄成同名纪录片（中译为《解构企业》），分析了现在一些具规模的国际集团怎样不平等地利用地球资源、剥削第三世界的劳工、破坏当地的天然资源，并以金钱为首的宗旨下罔顾消费者的生命、以权利把恶行遮掩。 <br />
<br />
150年前，企业还是相对不重要的机构，今日却无所不在。就像教会、君主政体和共产党主宰其它时代、其它地方，企业是今日的统治机构。本纪录片检视了现代企业的本质、演变、冲击和可能未来。　　　 　<br />
<br />
企业品牌化到了今天已经使得许多人习以为常，企业以其自身的理念、识别行为改变了许多人的想法。然而，这部记录片对这些企业品牌化污染行为加予评击。这个全新的视角在确切的事实数据基础上，让我们认识到了企业在资源掠夺、生态平衡、政治强权等方面的影响。 <br />
<br />
导演运用讥讽的叙述手法，在影片一开始，便开宗明义地透过心理学家Robert Hare，试图以世界卫生组织及DSMIV（心理疾病诊断统计手册）的诊断标准，来判断这些财团法人或大企业极可能是罹患了精神疾病，征状有不关心其它生物的感受、人际关系不持久、漠视他人安危、为自我利益而持续诈欺、无焦虑及罪恶感、反社会人格等等。然而，在这个全球化所向披靡的时代，具有这些人格特质的跨国企业竞相掠夺，撤底的冲击了整个环境、儿童、媒体，乃至基因，同时也带来了巨大的灾难与风险。<br />
