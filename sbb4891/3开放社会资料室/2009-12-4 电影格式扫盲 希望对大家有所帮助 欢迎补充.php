													<h2>电影格式扫盲 希望对大家有所帮助 欢迎补充</h2>
						
						
																											<div id="postmessage_354703" class="t_msgfont">如果你常常望着电影视频文件那一大长串的字母不知所措，不知道他们当中的每一个后缀分别代表了什么样的含义，哪个版本是最好的，那就来看看这篇文章吧。这里是本人从网上收集而来的有关电影版本方方面面的信息，希望能够对您有所帮助。 <br />
 <br />
1.CAM（枪版）――珍爱生命，远离枪版 　　 <br />
　　CAM通常是用数码摄像机从电影院盗录。有时会使用小三角架，但大多数时候不可能使用，所以摄像机会抖动。因此我们看到画面通常偏暗人物常常会失真，下方的字幕时常会出现倾斜。由于声音是从摄像机自带的话筒录制，所以经常会录到观众的笑声等声音。因为这些因素，图象和声音质量通常都很差。 <br />
　　举例说明：Journey.To.The.Center.Of.The.Earth.CAM.XViD-CAMERA（地心游记3D枪版） <br />
　　 <br />
　　2.TS――只比枪版好一点 　　 <br />
　　TS与CAM版的标准是相同的。但它使用的是外置音源（一般是影院座椅上为听力不好的人设的耳机孔）这个音源不能保证是好的音源，因为受到很多背景噪音的干扰。TS是在空的影院或是用专业摄像机在投影室录制，所以图象质量可能比CAM好。但画面的起伏很大。常出现的有一般TS版和经过修复清晰TS版。 <br />
　　举例说明：Iron.Man.PROPER.TS.XviD-iLG（钢铁侠TS版） <br />
　　 <br />
　　3.TC――基本也可以忽略 　　 <br />
　　TC使用电视电影机从胶片直接数字拷贝。画面质量还不错,但亮度不足，有些昏暗。很多时候制作TC使用的音源来自TS，因此音质很差，但画面质量远好过TS。如果不是太讲究的话TC版还是不错的选择。 <br />
　　举例说明：Madagascar.2005.TC.XviD.AC3.avi（马达加斯加TC版） <br />
　　 <br />
　　4.DVDSCR――最差也要看这个 　 <br />
　　预览版的或者是测试版的DVD，非正式出版的版本。从预览版 DVD 中获取，通过mpeg-4技术进行高质量压缩的视频格式。能比DVDRip早发布，但画质稍差。（经常有一些不在黑边里在屏幕下方滚动的消息，包含版权和反盗版电话号码 ，会影响观看。）如果没有严格的划分它的画质应与TC版差不多。 <br />
　　举例说明：Quantum.of.Solace.REPACK.DVDSCR.XviD-COALiTiON.avi（007大破量子危机DVDSCR版） <br />
　　 <br />
　　5.DVDRIP――比较理想的版本 　　 <br />
　　DVDRIP:是从最终版的DVD转制。质量应该是最好的。将DVD的视频、音频、字幕剥离出来，再经过压缩或者其他处理，然后重新合成成多媒体文件。一般来说，DVDrip由影音文件(后缀为avi)和字幕文件组成。 　 <br />
　　举例说明： Pineapple.Express.2008.DVDRip.XviD-ARROW.avi（菠萝快车DVDRip版） <br />
 <br />
6.HR-HDTV――不错的选择 　 <br />
　　HR-HDTV是用DivX/XviD/x264等MPEG4压缩技术对HDTV的视频图像进行高质量压缩,然后将视频、音频部分封装成一个.avi或.mkv文件，最后再加上外挂的字幕文件而形成的视频格式。 画面清晰度更高。 <br />
　　 <br />
　　7.HDRIP――BD之下，万人之上 　　 <br />
　　HDRIP=HD-DVD Rip，即HD-DVD高清转制，效果好于一般的高清转制。 <br />
　　举例说明：<a href="mailto:Stardust.2007.HDRip.x264.a720.AC3-C@SiLU.mkv">Stardust.2007.HDRip.x264.a720.AC3-C@SiLU.mkv</a>（星尘HDrip版） <br />
　　 <br />
　　8.BDRIP――高清之下，普通清晰度的王者 　　 <br />
　　BDrip = Blue-ray Disc Rip，即蓝光高清转制，效果比HDRIP还要好。 <br />
　　举例说明：Awake.2007.BDRip.X264-TLF.mkv（夺命手术BDrip版） <br />
　　 <br />
　　9.R5――心急的您可以看这个 　　 <br />
　　R5一般大都是俄罗斯5区版，由于北美大片在俄罗斯发行的语种经常配音为俄罗斯语，因此网络上会出现采用R5的视频＋枪版TS的英语原声音频＝合成版本（所以音效差），当然，也经常有未配音过音效好的R5，遇到合成版本，在发布时会特别注明音效。 <br />
　　举例说明：Get.Smart.R5.LINE.XViD-mVs.avi（糊涂侦探R5版） <br />
　　 <br />
　　10.AVI――廉颇老矣 　　 <br />
　　AVI英文全称为Audio Video Interleaved，即音频视频交错格式。是将语音和影像同步组合在一起的文件格式。它对视频文件采用了一种有损压缩方式，但压缩比较高，因此尽管面面质量不是太好，但其应用范围仍然非常广泛。（就目前来看，随着观众对电影品质要求的提升，AVI格式越来越力不从心了） <br />
　　举例说明：Breaking.News.2004.DVDRip.XViD-TLF.avi（大事件avi格式版本） <br />
 <br />
11.MKV――后起之秀 　　 <br />
　　一种后缀为MKV的视频文件频频出现在网络上，它可在一个文件中集成多条不同类型的音轨和字幕轨，而且其视频编码的自由度也非常大，可以是常见的 DivX、XviD、3IVX，甚至可以是RealVideo、 QuickTime、WMV这类流式视频。实际上，它是一种全称为Matroska的新型多媒体封装格式，这种先进的、开放的封装格式已经给我们展示非常好的应用前景，甚至有人把它看成是AVI的替代者！ 　　 <br />
　　举例说明：Children.Of.Man.2006.BDRE.1080p.x264.AC3-SiLUHD.mkv（人类之子MKV封装版本） <br />
　　 <br />
　　12.MOV――高清预告片首选 　　 <br />
　　MOV即QuickTime影片格式，它是Apple公司开发的一种音频、视频文件格式，用于存储常用数字媒体类型，如音频和视频。当选择 QuickTime （*.mov）作为“保存类型”时，动画将保存为 .mov 文件。现在MOV格式常常被用来制作高清电影预告片，可以达到1080P的所谓全高清标准。 　　 <br />
 <br />
　　举例说明：terminatorsalvation-tlr1_h1080p.mov（终结者4高清预告片） <br />
　　 <br />
　　13.XviD――常见标准 　　 <br />
　　最常用的MPEG-4视频压缩编码格式，我们下载的很多电影基于这种编码。 <br />
 <br />
举例说明：Connected.2008.DVDRip.XviD-BiEN.avi（保持通话 xvid编码）　　 <br />
　　14.X264――业界先锋 　　 <br />
　　x264是一种免费的、具有更优秀算法的H.264/MPEG-4 AVC视频压缩编码格式。它同xvid一样都是开源项目，但x264是采用H.264标准的，而xvid是采用MPEG-4早期标准的。由于H.264是 2003年正式发布的最新的视频编码标准，因此，在通常情况下，x264压缩出的视频文件在相同质量下要比xvid压缩出的文件要小，或者也可以说，在相同体积下比xvid压缩出的文件质量要好。 　　 <br />
　　举例说明：WALL.E.2008.BDRip.X264-TLF.mkv（机器人总动员，X264编码） <br />
　　 <br />
　　15.IMAX――所谓巨幕 　　 <br />
　　有的电影会标注“IMAX”版本。那什么是IMAX呢？ 　　 <br />
　　IMAX（即Image Maximum的缩写，意为“最大影像”，汉语发音可读作“艾麦克斯”)是一种能够放映比传统胶片更大和更高解像度的电影放映系统。整套系统包括以 IMAX规格摄制的影片拷贝、放映机、音响系统、银幕等。标准的IMAX银幕为22米宽、16米高，但完全可以在更大的银幕播放，而且迄今为止不断有更大的IMAX银幕出现。 今年的《功夫熊猫》就同时具有普通和IMAX两种版本。 　　 <br />
　　举例说明：The.Dark.Knight.2008.IMAX.BDRip.X264-TLF.mkv（蝙蝠侠黑暗骑士IMAX版本） <br />
 <br />
16.MP4――比较尴尬 　　 <br />
　　有的电影是MP4格式的，这代表着它采用了MPEG-4视频压缩编码，但是具体是哪一种编码格式并不确定，一般来说是DIVX或者XVID。总之感觉是个很鸡肋的格式，一般是用在掌上便携设备上，电脑用户不推荐。 　　 <br />
　　举例说明：Friends.S01E01.The.One.Where.Monica.Gets.A.New.Roommate.iPod-mp4.chs-en.mp4（老友记第一季第一集，ipd专用版本，mp4格式） <br />
　　 <br />
　　17.TS――此TS非彼TS 　　 <br />
　　这个TS代表的是文件后缀名为.TS的高清格式。这种格式的优点在于对硬件的要求不高，即使是1080P的电影这两年的家用电脑都可以轻松播放，不过缺点同样明显――超大的文件体积，动辄三四十个G。 <br />
　　举例说明：<a href="mailto:Silent.Hill.Blu-ray.REMUX.MPEG2.1080P.LPCM.DTS.Orbitlee@Silu.ts">Silent.Hill.Blu-ray.REMUX.MPEG2.1080P.LPCM.DTS.Orbitlee@Silu.ts</a>（寂静岭TS版，9碟，完全大小37.9G，单碟4.3G） <br />
　　 <br />
　　18.PROPER――竞争的结果 　　 <br />
　　根据发布规则，最先发布Telesync (TS)的组织赢得(TS发布的)比赛。但是，如果这个发布版本质量很差，同时另一组织有另一TS版本(或质量更好的同一片源)，那么标记PROPER被加到目录上以避免重复。PROPER是一个最主观的标记，很多人会争论是否PROPER比原始发布版本好。很多发布组织只不过因为输掉了发布比赛而发布 PROPER。发布PROPER的原因应该总是包含在NFO文件里。 　　 <br />
　　举例说明：Pirates.Of.The.Caribbean.At.Worlds.End.PROPER.DVDRip.XviD-HooKah-CD1.avi（加勒比海盗3世界尽头proper版本） <br />
　　 <br />
　　19.Director’s Cut――导演剪辑版 　　 <br />
　　导演剪辑版，主要用于碟片发行中，因为出于商业的、票房的原因，在影院上映的影片未必是导演最终剪辑后的版本。故而碟商们推出了所谓导演剪辑版，与影院版有所区别，也是为了满足一部分追求“原汁原味”的观众的要求，同时多了一条生财之道。导演剪辑版与影院版，有的差别较大（无论是放映时间还是镜头），有的则差别不大，因人因片而异。至于艺术成就，更是很难笼统而言。因为有人看重导演的主动权，但导演剪辑版也可能不如影院版更精炼，见仁见智而已。 　　 <br />
　　举例说明：Saw.Director’s.Cut.HD.DVD.REMUX.VC1.1080p.DTS-HD.Silu.ts（电锯惊魂导演剪辑版） <br />
　　 <br />
　　20.Unrated――未分级版 　　 <br />
　　也就是没有根据不同观众对象剪辑，比较完整、原始的版本。（俗称未剪版）。 　　 <br />
 <br />
指的是没有在MPAA（美国电影协会）审查的电影。现在指的是一般的电影的最初为剪辑版本。一般包括大量暴力色情粗口的。是导演最初剪出来的片子。经MPAA审了片子之后就会定级。诸如。R、NC-17、PG13、PG、G （越往后越合家欢）等。 <br />
 <br />
举例说明：American.Psycho.Unrated.Version.2000.DVDrip.XviD.AC3.iNT.avi（美国狂魔未分级版） <br />
 <br />
　21.LIMITED 　　 <br />
　　LIMITED电影指该电影只在有限的电影院放映，通常少于250家。通常较小的电影（比如艺术电影）的发行是LIMETED。 　　 <br />
　　举例说明：Eternal.2004.LiMiTED.DVDrip.XviD-ShysteR.avi（邪恶的永生limited版） <br />
　　 <br />
　　22.TVRIP――TV转制 　　 <br />
　　所谓TVRip是从电视(最好是从数码有线电视/卫星电视捕捉)转制的电视剧及接收卫星接收到的节目，然后通过电视卡进行捕捉，压缩成文件，我们看的很多综艺及体育节目都是TVRip。有些电视剧也会使用TVRip的方式进行发布。 <br />
　　 <br />
　　23.　720P 　　 <br />
　　720P是美国电影电视工程师协会(SMPTE)制定的最高等级高清数字电视的格式标准，有效显示格式为：1280×720.SMPTE(美国电影电视工程协会)将数字高清信号数字电视扫描线的不同分为1080P、1080I、720P(i是interlace,隔行的意思，p是 Progressive,逐行的意思)。720P是一种在逐行扫描下达到1280×720的分辨率的显示格式。是数字电影成像技术和计算机技术的融合。这是高清视频的最低标准。 <br />
 <br />
举例说明：Le.Fabuleux.Destin.d’Amé<a href="mailto:lie.Poulain.2001.Blu-ray.720p.x264.DD51-HiS@SiLUHD.mkv">lie.Poulain.2001.Blu-ray.720p.x264.DD51-HiS@SiLUHD.mkv</a>（天使爱美丽720P） <br />
　　 <br />
　　24.　1080I 　　 <br />
　　即1080线隔行扫描，分辨率为1920×1080，仅次于1080P 　　 <br />
　　举例说明：close.encounters.of.the.third.kind.1080i.disk1.ts（第三类接触1080i版本） <br />
　　 <br />
　　25.　1080P 　　 <br />
　　1080P是美国电影电视工程师协会（SMPTE）制定的最高等级高清数字电视的格式标准，有效显示格式为：1920×1080，像素数达到207．36万。 　　 <br />
　　1080P带来的高画质给消费者带来的是真正的家庭影院的视听享受，由于它向下全面兼容其它高清格式，通用性非常强，保证了在未来十几年的时间里产品不会过时，具有很好的超前性。随着1080P片源的不断涌现，1080P产品的优势已经显现出来。 <br />
举例说明：<a href="mailto:Resident.Evil.HD.DVD.REMUX.1080p.MPEG-2.DTS-HD.F@Silu.disk1.ts">Resident.Evil.HD.DVD.REMUX.1080p.MPEG-2.DTS-HD.F@Silu.disk1.ts</a>（生化危机1，1080P版本） <br />
 <br />
26.　REPACK――重新打包版 　 <br />
　　如果发布组织发布了一个坏的版本，他们会发布REPACK来解决这些问题。 <br />
 <br />
举例说明：The.Eye.2008.REPACK.BDRip.X264-TLF-cd1.mkv（美版见鬼 repack版本） <br />
　　 <br />
　　27.　REMUX――无损版 　　 <br />
　　Remux的意思是无损的提取出HD-DVD 和 BluRay-DVD里面的视频数据和音频数据，封装到我们熟悉的TS或者AVI文件中。 　　 <br />
　　HD-DVD和 Blu-Ray Rip、Re都是把原始的高清数据进行压缩和重编码，从1080P压缩到720P, 20G左右的片子压缩到了4.3G. 可在19、20、22寸的宽屏上看，但是上到大尺寸高分辩率的显示设备就能明显的看出区别了。大屏幕的朋友还是建议购买原始编码的Remux。 　　 <br />
　　举例说明：<a href="mailto:The.Patriot.Extended.Cut.Blu-ray.REMUX.H264.1080P.DTS.LPCM.DD51.Fanxy@Silu.DISK1.ts">The.Patriot.Extended.Cut.Blu-ray.REMUX.H264.1080P.DTS.LPCM.DD51.Fanxy@Silu.DISK1.ts</a>（爱国者remux版本） <br />
　　 <br />
　　28. 　Extended――加长版 　　 <br />
　　情形和导演剪辑版类似，很多的加长版是用来圈钱的,有的干脆就是把花絮里的删除段落直接加进去,也就长了几分钟而已。也有很厚道的,比如指环王的加长版,一部就加长了近50分钟,肯定对剧情有影响的。 　　 <br />
　　举例说明：The.Lord.Of.The.Rings-Return.Of.The.King.EXTENDED.EDITION.DVDRip.XViD-ALLiANCE.avi（指环王3：王者归来加长版） <br />
　　 <br />
　　29.　AC3 　　 <br />
　　常见的电影音频格式。 　　 <br />
　　举例说明：Crank.2006.DVDRip.XviD.AC3-lsqh2002.avi（怒火攻心，音频格式AC3） <br />
　　 <br />
　　30.　DD5.1――杜比5.1声道 　　 <br />
　　5.1声道就是使用5个喇叭和1个超低音扬声器来达到一种身临其境感觉的音乐播放方式，由杜比公司开发。一般来讲，只有高清视频才采用这种音轨。 　　 <br />
　　举例说明：The.Messenger.The.Story.of.Joan.of.Arc.Blu-ray.RE.1080.x264.DD51.SiLUHD-001.mkv（圣女贞德，音频部分DD5.1）</div>

							
							
							
							
