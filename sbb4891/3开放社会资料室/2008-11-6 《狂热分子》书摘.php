													<h2>《狂热分子》书摘</h2>
						
						
																											<div id="postmessage_3562" class="t_msgfont">（本书主要探讨群众运动的一些共有特征，重点是陷入狂热的乌合之众的人格。长期与下层民众打交道的生活经历使作者发现，积极投身群众运动的往往是一些失意者。他们认为自己的生活已无可救药地失败，渴望逃离自我寻求重生，将生命托付给某项神圣伟业让他们感觉不错，整齐划一的集体生活令个人的责任、恐惧、无能得以掩埋。运动的领导者则刻意培养参与者的罪恶感，号召自我牺牲以获救赎。<br />
<br />
　　自1951年出版后，本书即被视为社会科学领域的经典之作，短期内行销50万册以上，被译成10余种语言，是多所大学政治系的必读书。书中俯拾皆是充满智慧的思想火花与一针见血的比喻，风格酷似法国思想家蒙田与帕斯卡尔的随笔。至今，其佳言警句仍不断被引用、辑录。<br />
<br />
作者埃里克・霍弗（Eric Hoffer，1902-1983）的一生十分传奇，7岁失明，15岁复明，父母早逝，靠自学成就学问。长年从事码头搬运工作，1964年成为加州伯克利大学高级研究员后，仍未离开码头，其许多观念都是在码头工作中形成，因此他也被称为“码头工人哲学家”。1982年获里根总统颁赠总统自由奖章。生平著作超过10部，包括《激情心灵状态》、《变迁的磨难》、《我们时代的脾性》等，曾多次获美国国家图书奖，《狂热分子》是他第一部和最著名的作品。）<br />
<br />
书摘一<br />
一头栽进某种变革大业里的人，都必然怀有极度不满情绪而又不是一贫如洗，都必然相信某种万能的教义、某个不会犯错的领袖或某种新技术已经给了他们所向无敌的力量。另外，他们必然抱有极不切实际的憧憬和深信未来具有无限可塑性。最后，他们对他们要做之事所涉及的困难也必然一无所知。<br />
<br />
用一个群众运动压制另一个群众运动并非是没有危险的，代价也不一定便宜。<br />
<br />
我们参加群众运动，是为了逃避个人责任或为了得到――用一个热情洋溢的年轻纳粹党员的话说――“免于自由的自由”。<br />
<br />
群众运动虽然往往打着自由的旗号对抗高压秩序，但它们全速启动以后，却不会让个人自由有实现的余地。这是因为，当一个群众运动在与既有秩序作生死斗争，或是抵抗内外地人以求自存时，它的首要之务实建立团结性和自我牺牲精神，而两样东西都需要个人放弃其意志、判断与利益。<br />
<br />
（群众运动的主体是一些“心怀不平”者，他们常见于：穷人、畸零人、被遗弃的人、少数民族、青春期的少年、有野心的人、被某些恶德或偏执挟制的人、无能者、极度自私的人、对生活厌烦的人呢、罪犯）<br />
<br />
暂时性的畸零人：青春期的少年、失业的大学毕业生、退伍军人、新移民等皆属此类。这类人很容易受群众运动的宣传感染，但却不总会成为坚定不移的信徒。这是因为他们并未完全疏离自我，并未把自己的人生视为一败涂地、无可挽回。……只要有一点点进步和成就，就足以使他们和这个世界以及自我取得协调。永久性的畸零人：在永久性的畸零人中，失意感最深度是那些有创作渴望却无法满足的人。……大部分的纳粹头子都有艺术和文学上的野心，却没有实现的能力。<br />
<br />
极端自私的人特别容易有失意感。一个人愈自私，失望时就愈难熬。因此，极端自私的人往往是无私精神最勇猛的捍卫者。最凶暴的狂热者，往往是一些本来自私但却出于某些原因而被迫失去对“自我”的信仰的人。于是，他们不再把高明的利己手段拿来服务其无能的自我，转而用于服务一件神圣事业。但即便他们信奉的是一种鼓吹爱与谦卑的宗教，他们既不会去爱，也不会谦卑。<br />
<br />
抗高压电能力部分源自一个人对群体的认同感。在纳粹集中营中，最能挺得煎熬的是那些觉得自己是隶属某个党、教会或民族主义团体的人。反之，所有个人主义者（不分国籍）全都陷于崩溃边缘。由此我们可以得到一个推论：碰到苦难和死亡的威胁，个人是无法依赖自己的勇气的。他仅有的力量来源不在他自身，而在于他隶属于某个伟大的、光荣的、不可摧毁的集体。……<br />
<br />
奇怪的是，这些在斯大林的秘密警察面前俯首贴耳的人，面对纳粹入侵时却变现出无比的勇气。造成这种差别的理由不在于斯大林的秘密警察比纳粹军队更为残酷，而在于他们是以孤立个人的身份面对秘密警察，但面对纳粹时却自感是一个伟大民族的一员，这民族拥有一个光荣的过去和一个更光荣的未来。<br />
<br />
失意者会在大混乱和有钱人的没落中得到快乐，不是因为他们意识到他们已经为兴建一个新世界铲除了一切障碍。他们固然狂热地欢呼“要么一切都变得美好，要么一物不留”，但他们心里真正热望的，大概是“一物不留”。<br />
<br />
因为自我牺牲乃是一种不合理的行为。它不可能是经过探索和深思的结果。所以，所有群众运动莫不竭力在信徒与真实世界之间拉上一道帷幕。<br />
<br />
狂热者无法被说服，只能被煽动。对他而言，真正重要的不是他所依附的大业的本质，而是他渴望有所依附的情感需要。<br />
<br />
在所有团结的催化剂中，最容易运用和理解的一项，就是仇恨。仇恨可以把人从他的自我快速卷走，使他忘记自己的幸福和前途，不去妒忌他人也不会只顾自己。他会变成一颗匿名的粒子，渴望跟同类汇聚融合，形成一个发光发热的集体。<br />
<br />
那些精明得知道该怎样发动或推进一个群众运动的人，除了知道该提出何种主义纲领以外，还懂得怎样挑选一个敌人。<br />
<br />
恨一个有不少优点的敌人要比恨一个一无是处的敌人容易。我们无法恨那些我们鄙夷的人。日本人占便宜的地方是，他们钦佩我们美国人要多于我们钦佩他们。因此，他们可以狂热地恨我们，而我们却无法多恨他们。……一个美国人对另外一个美国人的恨要比他对任何外国人的恨更凶狠。……要是哪一天美国人开始全心全意去恨外国人，就反映出他们已经失去自信心。<br />
<br />
炽烈的恨意可以给空虚的生活带来意义和目的。因此，一个漫无目标的人要是想为生活找到新意义，办法除了现身于一种神圣大业外，还可以培养狂热的悲愤。群中运动在这两方面都提供了无穷机会。<br />
<br />
当我们抛弃自我，成为一个紧密团体的一部分时，我们不仅抛开了个人利益，同时也抛开了个人责任。……当我们在群众运动中丧失了自我独立性，我们就得到一种新自由――一种无愧无疚地去恨、去恫吓、去撒谎、去凌虐、去背叛的自由。<br />
<br />
因此，当强制手段好不容情和持续不休的时候，它会产生一种无与伦比的说服力，而且不只头脑简单的人会被说服，连那些以才智自傲的人亦复如此，例如，当俄国的科学家、作家和艺术家被克里姆林宫要求放弃信念和承认错误时，他们十之八九都是真心悔罪，而不只是口头敷衍。<br />
<br />
在危机时期（水灾、地震、瘟疫、经济萧条、战争等），因为单打独斗是不起作用的，所以各种个性、处境的人都会愿意服从和追随一位领袖。<br />
<br />
