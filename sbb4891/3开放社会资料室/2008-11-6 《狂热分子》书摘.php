群众运动不需要相信有上帝，却不能不相信有魔鬼 <br />
<br />
在所有团结的催化剂中，最容易运用和理解的一项，就是仇恨。仇恨可以把人从他的自我中快速卷走，使他忘记自己的幸福和前途，不去妒忌他人也不会只顾自己。他会变成一颗匿名的粒子，渴望跟同类汇聚融合，形成一个发光发热的集体。海涅说过，基督宗教的爱所无能为力的事，可以靠一种共同的仇恨去做到。 <br />
<br />
群众运动不需要相信有上帝，一样可以兴起和传播，但它却不能不相信有魔鬼。通常，一个群众运动的强度跟这个魔鬼的具体性与鲜明度成正比。希特勒被问到有没有考虑过要把犹太人全部消灭时，他回答说：“没有……那样我们势必得另外创造一个犹太人。要紧的是有一个具体的敌人，而不仅是一个抽象的敌人。”1932年，有一个日本使团造访柏林，研究纳粹运动。当福格特问其中一个成员对纳粹运动有何观感时，对方回答说：“真是了不起。但愿我们在日本也能有类似的运动，只是我们做不到，因为日本并没有犹太人。”那些精明得知道该怎样发动或推进一个群众运动的人，除了知道该提出何种主义纲领以外，还懂得怎样挑选一个敌人。里姆林宫的理论家显然就是个中高手：他们不等第二次世界大战结束，就选定西方（特别是美国）作为敌人。 <br />
<br />
蒋介石的一大败笔就是八年抗战结束后，未能及时找到一个代替日本人的新魔鬼。这位雄心勃勃却头脑简单的将军大概是太自负了，以致未能了解，会让中国群众团结起来的因素并不是他，而是日本“魔鬼”所激起的民族激情。 <br />
<br />
恨是最有力的凝聚剂，自卑常常转化为对他人的怨恨 <br />
<br />
我们爱一个对象的时候，一般不会寻找同好，甚至反而会把跟我们爱同一对象的人视为竞争者和侵犯者。但我们恨一个对象时，却总是会寻求有志一同的人。 <br />
<br />
以下的道理不难理解：当我们受到伤害、渴望报复时，总会希望有别人站在我们同一边。但让人费解的是，当我们的恨不是出于明显的理由，而且看来是站不住脚的时候，渴望盟友的心理会更形迫切。而这种恨，也是最有力的凝聚剂之一。 <br />
<br />
这一类不可理喻的仇恨是从何而来，它们又为什么能发挥凝聚的作用？它们是我们拼命要掩盖自己贫乏、无价值、罪恶感和其他缺点的一种表现。自卑就此被转化为对他人的怨恨。而为了掩饰这种转化，我们会作出最坚决和最持久的努力。显然，最有效的掩饰方式就是找一些人―――愈多愈好―――来恨我们所恨的同一对象。 <br />
<br />
哪怕是在合理悲愤的情况下，我们的恨意主要仍然不是来自别人对我们的伤害，而是来自我们意识到自己的无能和懦弱―――换言之是源于自卑。当我们自感比伤害我们的人优越，我们更可能的反应是鄙夷他们，甚至是可怜他们，而不是恨他们。悲愤与恨意的关系并不是简单直接这一点，还可以从一个事实反映出来：我们释放出的恨意并不总是指向那些伤害我们的人。情况往往是，被一个人伤害之后，我们会恨的是一个完全不相干的人。例如，遭斯大林秘密警察欺压的俄国人轻易就被煽动去反对“资本主义战争贩子”；因为《凡尔赛和约》心生不满的德国人把气出在犹太人身上；美国南方的贫穷白人饱受特权阶级的气，却拿黑人来当出气筒。 <br />
<br />
