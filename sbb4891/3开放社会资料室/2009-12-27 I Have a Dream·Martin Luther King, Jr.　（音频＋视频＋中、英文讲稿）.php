　　100年前，一位伟大的美国人――今天我们就站在他象征性的身影下――签署了《解放宣言》。这项重要法令的颁布，对于千百万灼烤于非正义残焰中的黑奴，犹如带来希望之光的硕大灯塔，恰似结束漫漫长夜禁锢的欢畅黎明。 <br />
<br />
　　然而，100年后，黑人依然没有获得自由。100年后，黑人依然悲惨地蹒跚于种族隔离和种族歧视的枷锁之下。100年后，黑人依然生活在物质繁荣翰海的贫困孤岛上。100年后，黑人依然在美国社会中间向隅而泣，依然感到自己在国土家园中流离漂泊。所以，我们今天来到这里，要把这骇人听闻的情况公诸于众。 <br />
<br />
　　从某种意义上说，我们来到国家的首都是为了兑现一张支票。我们共和国的缔造者在拟写宪法和独立宣言的辉煌篇章时，就签署了一张每一个美国人都能继承的期票。这张期票向所有人承诺――不论白人还是黑人――都享有不可让渡的生存权、自由权和追求幸福权。 <br />
<br />
　　然而，今天美国显然对她的有色公民拖欠着这张期票。美国没有承兑这笔神圣的债务，而是开始给黑人一张空头支票――一张盖着“资金不足”的印戳被退回的支票。但是，我们决不相信正义的银行会破产。我们决不相信这个国家巨大的机会宝库会资金不足。 因此，我们来兑现这张支票。这张支票将给我们以宝贵的自由和正义的保障。 <br />
<br />
　　我们来到这块圣地还为了提醒美国：现在正是万分紧急的时刻。现在不是从容不迫悠然行事或服用渐进主义镇静剂的时候。现在是实现民主诺言的时候。现在是走出幽暗荒凉的种族隔离深谷，踏上种族平等的阳关大道的时候。现在是使我们国家走出种族不平等的流沙，踏上充满手足之情的磐石的时候。现在是使上帝所有孩子真正享有公正的时候。 <br />
<br />
　　忽视这一时刻的紧迫性，对于国家将会是致命的。自由平等的朗朗秋日不到来，黑人顺情合理哀怨的酷暑就不会过去。1963年不是一个结束，而是一个开端。 <br />
<br />
　　如果国家依然我行我素，那些希望黑人只需出出气就会心满意足的人将大失所望。在黑人得到公民权之前，美国既不会安宁，也不会平静。反抗的旋风将继续震撼我们国家的基石，直至光辉灿烂的正义之日来临。 <br />
<br />
　　但是，对于站在通向正义之宫艰险门槛上的人们，有一些话我必须要说。在我们争取合法地位的过程中，切不要错误行事导致犯罪。我们切不要吞饮仇恨辛酸的苦酒，来解除对于自由的饮渴。 <br />
<br />
　　我们应该永远得体地、纪律严明地进行斗争。我们不能容许我们富有创造性的抗议沦为暴力行动。我们应该不断升华到用灵魂力量对付肉体力量的崇高境界。 席卷黑人社会的新的奇迹般的战斗精神，不应导致我们对所有白人的不信任――因为许多白人兄弟已经认识到：他们的命运同我们的命运紧密相连，他们的自由同我们的自由休戚相关。他们今天来到这里参加集会就是明证。 <br />
<br />
　　我们不能单独行动。当我们行动时，我们必须保证勇往直前。我们不能后退。有人问热心民权运动的人：“你们什么时候会感到满意？”只要黑人依然是不堪形容的警察暴行恐怖的牺牲品，我们就决不会满意。只要我们在旅途劳顿后，却被公路旁汽车游客旅社和城市旅馆拒之门外，我们就决不会满意。只要黑人的基本活动范围只限于从狭小的黑人居住区到较大的黑人居住区，我们就决不会满意。只要我们的孩子被“仅供白人”的牌子剥夺个性，损毁尊严，我们就决不会满意。只要密西西比州的黑人不能参加选举，纽约州的黑人认为他们 与选举毫不相干，我们就决不会满意。不，不，我们不会满意，直至公正似水奔流，正义如泉喷涌。 <br />
<br />
　　我并非没有注意到你们有些人历尽艰难困苦来到这里。你们有些人刚刚走出狭小的牢房。有些人来自因追求自由而遭受迫害风暴袭击和警察暴虐狂飙摧残的地区。你们饱经风霜，历尽苦难。继续努力吧，要相信：无辜受苦终得拯救。 回到密西西比去吧；回到亚拉巴马去吧；回到南卡罗来纳去吧；回到佐治亚去吧；回到路易斯安那去吧；回到我们北方城市中的贫民窟和黑人居住区去吧。要知道，这种情况能够而且将会改变。我们切不要在绝望的深渊里沉沦。 <br />
<br />
　　朋友们，今天我要对你们说，尽管眼下困难重重，但我依然怀有一个梦。这个梦深深植根于美国梦之中。 <br />
<br />
　　我梦想有一天，这个国家将会奋起，实现其立国信条的真谛：“我们认为这些真理不言而喻：人人生而平等。” <br />
<br />
　　我梦想有一天，在佐治亚州的红色山岗上，昔日奴隶的儿子能够同昔日奴隶主的儿子同席而坐，亲如手足。 我梦想有一天，甚至连密西西比州――一个非正义和压迫的热浪逼人的荒漠之州，也会改造成为自由和公正的青青绿洲。 <br />
<br />
　　我梦想有一天，我的四个小女儿将生活在一个不是以皮肤的颜色，而是以品格的优劣作为评判标准的国家里。 <br />
<br />
　　我今天怀有一个梦。 <br />
<br />
　　我梦想有一天，亚拉巴马州会有所改变――尽管该州州长现在仍滔滔不绝地说什么要对联邦法令提出异议和拒绝执行――在那里，黑人儿童能够和白人儿童兄弟姐妹般地携手并行。 <br />
<br />
　　我今天怀有一个梦。 <br />
<br />
　　我梦想有一天，深谷弥合，高山夷平，歧路化坦途，曲径成通衢，上帝的光华再现，普天下生灵共谒。 这是我们的希望。这是我将带回南方去的信念。有了这个信念，我们就能从绝望之山开采出希望之石。有了这个信念，我们就能把这个国家的嘈杂刺耳的争吵声，变为充满手足之情的悦耳交响曲。有了这个信念，我们就能一同工作，一同祈祷，一同斗争，一同入狱，一同维护自由，因为我们知道，我们终有一天会获得自由。 <br />
<br />
　　到了这一天，上帝的所有孩子都能以新的含义高唱这首歌： <br />
<br />
　　我的祖国，可爱的自由之邦，我为您歌唱。这是我祖先终老的地方，这是早期移民自豪的地方，让自由之声，响彻每一座山岗。 如果美国要成为伟大的国家，这一点必须实现。因此，让自由之声响彻新罕布什尔州的巍峨高峰！ <br />
<br />
　　让自由之声响彻纽约州的崇山峻岭！ <br />
<br />
　　让自由之声响彻宾夕法尼亚州的阿勒格尼高峰！ <br />
<br />
　　让自由之声响彻科罗拉多州冰雪皑皑的洛基山！ <br />
<br />
　　让自由之声响彻加利福尼亚州的婀娜群峰！ <br />
<br />
　　不，不仅如此；让自由之声响彻佐治亚州的石山！ <br />
<br />
　　让自由之声响彻田纳西州的望山！ <br />
<br />
　　让自由之声响彻密西西比州的一座座山峰，一个个土丘！ <br />
<br />
　　让自由之声响彻每一个山岗！ <br />
<br />
　　当我们让自由之声轰响，当我们让自由之声响彻每一个大村小庄，每一个州府城镇，我们就能加速这一天的到来。<br />
<br />
　　那时，上帝的所有孩子，黑人和白人，犹太教徒和非犹太教徒，耶稣教徒和天主教徒，将能携手同唱那首古老的黑人灵歌：<br />
<br />
　　“终于自由了！终于自由了！感谢全能的上帝，我们终于自由了！”</div>

							
							
							
							
