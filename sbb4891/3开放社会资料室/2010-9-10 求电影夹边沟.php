――政治灾难背后的人性悲剧<br />
<br />
程美信<br />
<br />
<br />
王兵导拍的《夹边沟》影片，成为追逐67届威尼斯电影节金狮奖的最后华语片，无论能否获得大奖，这部片子将再次考验着中国人的历史勇气和人性良知。<br />
<br />
这部极可能获本届影片届威尼斯电影节金狮奖的影片，根据作家杨显惠的历史纪事小说《夹边沟纪事》改编成剧。它的真实性，正是擅于驾驭纪事题材王兵的拿手戏，他拍摄《铁西区》便表现出一种忠于历史的艺术精神。当然，王兵在《夹边沟》中采取克制手法是不意外的，抑或在照顾到观众承受力，正如《夹边沟纪事》作者的写作过程，把文学创作成分压到最小程度，完全根据当事人回忆和口述来呈现当年夹边沟发生惨绝人寰的历史悲剧，它是由一条条活生生的人命构成的真实故事。<br />
<br />
在57年“引蛇出洞”的政治阳谋下，甘肃夹边沟农场关押了一大批政治改造犯，他们主要是国家干部和高级知识分子，有的满怀建设大西北崇高理想来到甘肃，自以为忠于党和国家，在“大放大鸣”英明号召下，向组织和领导提出各种意见；有的则是年轻气盛和禀性正直，对现实不满发表了一些牢骚；有的由于自身历史成份不好，加上那种生来俱有的阶级傲气，这些人全然被打成右派，送往劳改农场进行思想改造。随着“大跃进”引发全国饥荒，夹边沟劳改农场像是被人遗忘的死亡角落。犯人最初开始采集草根充饥，可营养不良到丧失体力，既不能逃亡而又无法觅食，最终纷纷饿死，并发展到吃人尸果腹的人道惨剧。总共三千多人的农场，竟然活活饿死了两千多人。<br />
<br />
迄今为止，中国人还没有勇气直面这场历史惨剧，从官方到民间都在有意识地在淡化“５７”、“大跃进”、“文革”的一系列人为政治灾难。当一个社会把正直善良、敢说真话的人们统统打倒，必是人人自危，类似爆发“大跃进”的惨绝人寰的悲剧是不可避免。“大跃进”期间每个公社都有“劳改队”，犯人主要对当时“浮夸风”表示不满，或者不积极配合，结果逮捕收押，集中进行劳动思想改造。当时推广的亩产万斤 “密植技术”，在任何一个农民看来都是荒谬不堪的瞎胡闹，可农民不仅沉默了，还将整担整担谷子挑到田坑去埋掉。在国家谎言和专制暴力下，“超英赶美”、“卫星亩产”、“大炼钢铁”如法炮制，正如肆虐的饥荒一般势不可挡。夹边沟劳改农场发生的人道灾难，只是整个“大跃进”悲剧的一个小缩影。<br />
<br />
王兵的《夹边过》之所以采取克制的纪实手法，试图通过客观而真实去反映那场人类历史上罕见的人道灾难，其思想主旨与《夹边沟纪事》作者一样，目的在于唤起人们对历史的反思认识，避免重蹈历史悲剧。假如中国人没有勇气直面这段惨痛的人为劫难，死于饥荒和迫害的人们，他们用生命的代价仍不足以唤醒国人的反思意识，那么国家谎言下的人祸劫难将源源不断。换言之，几千万条生命的沉痛代价都无法唤醒的历史勇气和人性良知，那这样的民族将不会持久的昌顺。 <br />
<br />
传来《夹边沟》影片极可能逐获本届威尼斯电影节金奖，以及境外媒体和专家对它评价甚高。国内媒体普遍持有一种冷淡态度，不象最初狂捧徐克的《狄仁杰》伪历史愚乐剧那样热闹。媒体的尴尬应变立即释放出一种麻木不仁的声调，用什么“观众倍感压抑，纷纷离场”形容《夹边沟》在威尼斯电影节的现场。但是，境外媒体报道完全不同，说王兵《夹边沟》赢得现场观众爆满掌声。很显然，纷纷离场的观众是一些前往威尼斯电影节为《狄仁杰》专门捧场的造势者。只能说这些人的政治规避意识太强，他们不可能有勇气面对象《夹边沟》的影片，意味着中国人既没有勇气面对历史，更没有摆脱类似“夹边沟”的可怕厄运。<br />
<br />
毫无疑问，《夹边沟》不论有没有获得威尼斯电影节大奖，但它至少在考验着中国人的历史勇气和人性良知，尤其政府必须检讨这场政治错误带来历史惨剧。面对《夹边沟》在电影节的“惊喜”，豆瓣论坛的网友们反应出一种复杂心情，对王兵的《夹边沟》持有否定态度的网友留言说：“靠禁忌吸引眼球吧”；“我觉得电影，不是靠禁忌啊、血腥啊、暴力啊什么的来吸引眼球的”；“我有预感，中国人民的感情又将被伤害”。对此持不同立场的网友担忧表示：“最烦这种‘靠禁忌吸引眼球’说法，言论自由、创作自由本来就是创作者的权利，是政治把正常的艺术创作变成禁忌，不去批评政治反过来怀疑艺术家的动机，这种逻辑真奇怪”；“有点担心此片可能会被河蟹”；“什么担心，百分百无悬念的被禁”；“估计这片子要拿下金狮，马克穆勒会获得和当年马悦然同一个待遇，拒绝入境”；“为什么一定要被禁？沙漠戈壁、政治事件多吸引人啊。又不是什么色情暴力、少儿不宜。咱们对政府太没信心了，以后这都不叫个事”；“威尼斯已经被中国人和谐了”。<br />
<br />
王兵面对“政治片”说，不得不出面阐明自己的立场，他说“政治片的定义是要反抗一个意见，我的电影谈得是这个国家在1949年后的十年中面临的问题。我们的思考不可能忽略政治的因素，但我只想很真实地交流，让大家看到那个时代中国人的生活情感”。此外，王兵也认为《夹边沟》是一部有建设性意义的历史电影，呼吁人们能够正视历史、尊重历史的电影，不是为了抗议任何东西，而是为了突出这部电影更加公正、更加自由。其实，包括作家杨显惠的《夹边沟纪事》出版发行，从中面临种种阻力困难，它说明了中国人还没有直面历史的文化勇气。如今《夹边沟》能不能在国内正式放映，成为人们普遍担心的焦点问题。不论电影《夹边沟》还是小说《夹边沟纪事》，它不止是中国政府有没有检讨重大历史错误的诚意，而是关系到中国社会今后的艺术自由、历史走向、反思勇气、文化良知和政治信心。要知道，那些祸饥荒和迫害中死去的几千万无辜亡灵，他们在屈辱与惨淡中奄奄一息，承受了生命所不堪承受的折磨，甚至没有一丝剧烈的挣抗，完全在绝望中死去。在政治灾难的背后，实际上人性冷漠造成的历史惨剧。<br />
