													<h2>小平同志感谢解放军指战员用这种态度来对待xx事件</h2>
						
						
																											<div id="postmessage_74617" class="t_msgfont">原题《在接见首都戒严部队军以上干部时的讲话》（一九八九年六月九日）选自：《邓小平文选》 第三卷<br />
<br />
同志们辛苦了！<br />
<br />
首先，我对在这场斗争中英勇牺牲的解放军指战员、武警指战员和公安干警的同志们表示沉痛的哀悼！对在这场斗争中负伤的几千名解放军指战员、武警指战员和公安干警的同志们表示亲切的慰问！对所有参加这场斗争的解放军指战员、武警指战员和公安干警的同志们致以亲切的问候！<br />
<br />
我提议，大家起立，为死难的烈士们默哀！<br />
<br />
利用这个机会，我讲几句话。<br />
<br />
这场风波迟早要来。这是国际的大气候和中国自己的小气候所决定了的，是一定要来的，是不以人们的意志为转移的，只不过是迟早的问题，大小的问题。而现在来，对我们比较有利。最有利的是，我们有一大批老同志健在，他们经历的风波多，懂得事情的利害关系，他们是支持对暴乱采取坚决行动的。虽然有一些同志一时还不理解，但最终是会理解的，会支持中央这个决定的。<br />
<br />
《人民日报》四月二十六日社论，把问题的性质定为动乱。“动乱”这两个字恰如其分，一些人反对的就是这两个字，要修改的也是这两个字。实践证明，这个判断是准确的。后来事态进一步发展到反革命暴乱，也是必然的。我们有一批老同志健在，包括军队，也有一批各个时期参加革命的骨干还在，因此，事情现在爆发，处理起来比较容易。处理这一事件的主要难点在于，我们从来没有遇到过这种情况，一小撮坏人混杂在那么多青年学生和围观的群众中间，阵线一时分不清楚，使我们许多应该采取的行动难以出手。如果没有我们党这么多老同志支持，甚至连事件的性质都难以确定。一些同志不了解问题的性质，认为这只是单纯的对待群众的问题，实际上，对方不只是一些是非不分的群众，还有一批造反派和大量的社会渣滓。他们是要颠覆我们的国家，颠覆我们的党，这是问题的实质。不懂得这个根本问题，就是性质不清楚。我相信，经过认真做工作，能取得党内绝大多数同志对定性和处理的拥护。<br />
<br />
事情一爆发出来，就很明确。他们的根本口号主要是两个，一是要打倒共产党，一是要推翻社会主义制度。他们的目的是要建立一个完全西方附庸化的资产阶级共和国。人民要求反腐败，我们当然接受。那些别有用心的人提出的所谓反腐败的口号，我们也要当好话来接受。当然，这个口号仅仅是他们的一个陪衬，而其核心是打倒共产党，推翻社会主义制度。<br />
<br />
这次平息暴乱中，我们那么多同志负了伤，甚至牺牲了，武器也被抢去了，这是为什么？也是因为好人坏人混杂在一起，使我们有些应该采取的断然措施难于出手。处理这件事对我们军队是一次很严峻的政治考验，实践证明，我们的解放军考试合格。如果用坦克压过去，就会在全国造成是非不清。所以，我要感谢解放军指战员用这种态度来对待暴乱事件。尽管损失是令人痛心的，但可以赢得人民，使是非不明的人改变观点。让大家看看，解放军究竟是什么人，有没有血洗天安门，流血的到底是谁。这个问题清楚了，就使我们取得了主动。虽然牺牲了许多同志非常令人痛心，但客观地分析事件的过程，人们就不得不承认，解放军是人民的子弟兵。这也有助于人民理解在这场斗争中我们所采取的方法，今后解放军遇到问题，采取措施，就都可以得到人民的支持了。这里顺便说一下，以后再不能让人把武器夺去了。总之，这是一个考验，考试是合格的。虽然军队里老同志不是很多了，战士们大都是十八九岁、二十岁出头的娃娃，但他们仍然是真正的人民子弟兵。在生命危险面前，他们没有忘记人民，没有忘记党的教导，没有忘记国家利益，面对死亡毫不含糊。慷慨赴死，从容就义，他们当之无愧。我讲考试合格，就是指军队仍然是人民子弟兵，这个性质合格。这个军队还是我们的老红军的传统。这次过的是真正的政治关、生死关，不容易呀！这表明，人民子弟兵真正是党和国家的钢铁长城。这表明，不管我们受到多么大的损失，不管如何更新换代，我们这个军队永远是党领导下的军队，永远是国家的捍卫者，永远是社会主义的捍卫者，永远是人民利益的捍卫者，是最可爱的人！同时，我们永远也不要忘记，我们的敌人是多么凶残，对他们，连百分之一的原谅都不应有。<br />
<br />
这次事件爆发出来，很值得我们思索，促使我们很冷静地考虑一下过去，也考虑一下未来。也许这件坏事会使我们改革开放的步子迈得更稳、更好，甚至于更快，使我们的失误纠正得更快，使我们的长处发扬得更好。今天我不可能展开来讲，只是提出课题。<br />
<br />
第一个问题，党的十一届三中全会制定的路线、方针、政策，包括我们发展战略的“三部曲”，正确不正确？是不是因为发生了这次动乱，我们制定的路线、方针、政策的正确性就发生问题？我们的目标是不是一个“左”的目标？是否还要继续用它作为我们今后奋斗的目标？这些大的问题，必须作出明确、肯定的回答。我第一个翻一番的目标已经完成了，第二个翻一番的目标计划用十二年完成，再往后五十年，达到一个中等发达国家的水平。这就是我们的战略目标。对此，我想我们做出的不是一个“左”的判断，制定的也不是一个过急的目标。因此，对第一个问题的回答，应当说，我们所制定的战略目标，现在至少不能说是失败的。在六十一年后，一个十五亿人口的国家，达到中等发达国家的水平，是了不起的事情。实现这样一个目标，应该是能够做到的。不能因为这次事件的发生，就说我们的战略目标错了。<br />
<br />
第二个问题，党的十三大概括的“一个中心、两个基本点”对不对？两个基本点，即四个坚持和改革开放，是不是错了？我最近总在想这个问题。我们没有错。四个坚持本身没有错，如果说有错误的话，就是坚持四项基本原则还不够一贯，没有把它作为基本思想来教育人民，教育学生，教育全体干部和共产党员。这次事件的性质，就是资产阶级自由化和四个坚持的对立。四个坚持、思想政治工作、反对资产阶级自由化、反对精神污染，我们不是没有讲，而是缺乏一贯性，没有行动，甚至讲得都很少。不是错在四个坚持本身，而是错在坚持得不够一贯，教育和思想政治工作太差。一九八○年元旦，我在政协讲话，讲了“四个保证”，其中有一条叫“艰苦奋斗的创业精神”。艰苦奋斗是我们的传统，艰苦朴素的教育今后要抓紧，一直要抓六十至七十年。我们的国家越发展，越要抓艰苦创业。提倡艰苦创业精神，也有助于克服腐败现象。建国以来我们一直在讲艰苦创业，后来日子稍微好一点，就提倡高消费，于是，各方面的浪费现象蔓延，加上思想政治工作薄弱、法制不健全，什么违法乱纪和腐败现象等等，都出来了。我对外国人讲，十年最大的失误是教育，这里我主要是讲思想政治教育，不单纯是对学校、青年学生，是泛指对人民的教育。对于艰苦创业，对于中国是个什么样的国家，将要变成一个什么样的国家，这种教育都很少，这是我们很大的失误。<br />
<br />
改革开放这个基本点错了没有？没有错。没有改革开放，怎么会有今天？这十年人民生活水平有较大提高，应该说我们上了一个台阶，尽管出现了通货膨胀等问题，但十年改革开放的成绩要充分估计够。当然，改革开放必然会有西方的许多坏的影响进来，对此，我们从来没有估计不足。八十年代初建立经济特区时，我与广东同志谈，要两手抓，一手要抓改革开放，一手要抓严厉打击经济犯罪，包括抓思想政治工作。就是两点论。但今天回头来看，出现了明显的不足，一手比较硬，一手比较软。一硬一软不相称，配合得不好。讲这点，可能对我们以后制定方针政策有好处。还有，我们要继续坚持计划经济与市场调节相结合，这个不能改。实际工作中，在调整时期，我们可以加强或者多一点计划性，而在另一个时候多一点市场调节，搞得更灵活一些。以后还是计划经济与市场调节相结合。重要的是，切不要把中国搞成一个关闭性的国家。实行关闭政策的做法对我们极为不利，连信息都不灵通。现在不是讲信息重要吗？确实很重要。做管理工作的人没有信息，就是鼻子不通，耳目不灵。再是绝不能重复回到过去那样，把经济搞得死死的。我提出的这个建议，请常委研究。这也是个比较急迫的问题，总要接触的问题。<br />
<br />
这是总结我们过去十年。我们的一些基本提法，从发展战略到方针政策，包括改革开放，都是对的。要说不够，就是改革开放得还不够。我们在改革中遇到的难题比在开放中遇到的难题要多。在政治体制改革方面有一点可以肯定，就是我们要坚持实行人民代表大会的制度，而不是美国式的三权鼎立制度。实际上，西方国家也并不都是实行三权鼎立式的制度。美国骂我们镇压学生，他们处理国内学潮和骚乱，还不是出动了警察和军队，还不是抓人、流血？他们是镇压学生和人民，而我们则是平息反革命暴乱。他们有什么资格批评我们！今后，在处理这类问题的时候，倒是要注意，一个动态出现，不要使它蔓延。<br />
<br />
以后我们怎么办？我说，我们原来制定的基本路线、方针、政策，照样干下去，坚定不移地干下去。除了个别语言有的需要变动一下，基本路线和基本方针、政策都不变。这个问题已经提出来了，请大家认真考虑一下。至于一些做法，如投资方向、资金使用方向等，我赞成加强基础工业和农业。基础工业，无非是原材料工业、交通、能源等，要加强这方面的投资，要坚持十到二十年，宁肯欠债，也要加强。这也是开放，在这方面，胆子要大一些，不会有大的失误。多搞一点电，多搞一点铁路、公路、航运，能办很多事情。钢，外国人判断我们将来需要一亿二千万吨，现在我们接近六千万吨，还差一半。如果在现有企业的基础上加以改造，增加两千万吨，就可少进口钢材。借点外债用在这些方面，也叫改革开放。现在的问题不是改革开放政策对不对，搞不搞，而是如何搞，开哪方面，关哪方面。<br />
<br />
要坚定不移地执行党的十一届三中全会以来制定的一系列路线、方针、政策，要认真总结经验，对的要继续坚持，失误的要纠正，不足的要加点劲。总之，要总结现在，看到未来。<br />
<br />
利用这个机会，我就讲这一点。</div>

							
							
							
							
