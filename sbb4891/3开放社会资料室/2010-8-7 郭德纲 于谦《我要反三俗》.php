于：三俗？ <br />
郭：三俗！庸俗、低俗、媚俗！ <br />
于：这么三俗。 <br />
郭：绝对要反三俗！把它记在心里面。 <br />
于：好好好。 <br />
郭：谦虚使人进步。屎人都能进步！ <br />
于：您说这太脏了，您这本身就叫三俗。 <br />
郭：我弄死你信吗？没挨过流氓打是吗？大花盆儿砸脑袋上哗哗流血，打得你眼珠子缝针，比杨乃武都冤。 <br />
于：啊？ <br />
郭：我让你看看我这纹身。 <br />
于：你光膀子干吗？我不瞧这个。 <br />
郭：敢说我三俗？三俗是我用来侮辱人的手段。说我不行知道吗？ <br />
于：说您不行？ <br />
郭：讨厌知道吗？我每天工作很忙，我再和你这无聊的人打交道，我怎么为人民服务？我怎么反三俗？ <br />
于：您是哪个单位的？ <br />
郭：管得着吗？ <br />
于：问你干什么工作的？ <br />
郭：呸―― <br />
于：你疯子啊？怎么意思？ <br />
郭：才看出来你，他们早看出来了。我常常在想 <br />
于：想什么啊？ <br />
郭：人呐，为什么这么不自重。我在单位里我也很着急，很多人不务正业。很多有偏差的事情需要我去纠正，但是我操心不过来呀。 <br />
于：什么事啊？ <br />
郭：单位……拿个电话，拿个手机在那儿说话。 <br />
于：对呀。 <br />
郭：你有点正事儿没有？手机……手机是用来干嘛的？ <br />
于：干吗的？ <br />
郭：为什么要发明手机？ <br />
于：为什么？ <br />
郭：发明手机的目的是让你们怎么用它实现四化！ <br />
于：用手机实现四化呀？ <br />
郭：不是让你聊天的。 <br />
于：甭走大字眼了。 <br />
郭：太三俗了，讨厌。我就不是科学家就算了。我要是科学家，我研究一种新的手机，我就让你们聊不了天。 <br />
于：什么样啊？ <br />
郭：手机第一要大，这么大个儿，像月饼盒子是的。兜里搁不开。第二有线连着，搁桌子上动不了。这研究出来社会又进步了。 <br />
于：啊？那社会就回去了知道吗？您这不就是有线电话吗？ <br />
郭：你怎么跟我对着干呢？ <br />
于：不是我对着，您说这不像话都。 <br />
郭：简直就是一个很三俗的人。 <br />
于：怎么三俗了这？ <br />
郭：太三俗了。错了不是，你这样做是错误的。 <br />
于：我不对啊？ <br />
郭：提升品位，要高雅。记住了，天网恢恢，肥而不腻。 <br />
于：你这什么啊？ <br />
郭：对演员来说应该要自重啊，我就知道有个演员一点儿都不自重。 <br />
于：是吗？ <br />
郭：到最后终于吊儿郎当入狱。 <br />
于：不对啊，锒铛入狱。 <br />
郭：这个演员叫吊儿。 <br />
于：演员名字也俗。 <br />
郭：我常常在想，走在街上，迎面来的人都让我觉着睁不开眼。 <br />
于：怎么看不惯呢？ <br />
郭：有的人穿着背心短裤就上街，是人吗？还有的人穿着睡衣睡裤就出来，不自重啊。还有到游泳池你看看，还都穿个游泳衣，要脸吗？ <br />
于：废话，你穿着棉袄游泳去啊？ <br />
郭：露着胳膊露着大腿，太三俗了。现在泳衣做得也不好吗。 <br />
于：怎么了？ <br />
郭：过去的泳衣很端庄多好啊。 <br />
于：是。 <br />
郭：过去那个泳衣，扒开泳衣才能看见屁股；现在这个扒开屁股才能看见泳衣。太三俗了。 <br />
于：也没有像您这样的满街上看屁股的。 <br />
郭：穿成那样你怎么出来？你又不是毛片儿演员？你又不是那些黄色录像带的主演？你又不是李丽珍、舒淇、饭岛爱、高树玛丽亚…… <br />
于：三俗不三俗我不知道，反正这片子你可没少看。 <br />
郭：太三俗了。 <br />
于：你就够俗的了。 <br />
郭：我是批判性的看。 <br />
于：这片子还能批判性的看。 <br />
郭：我要看她们堕落到什么程度，我熬点儿夜儿算什么，我反三俗了我！ <br />
于：就干这个。 <br />
