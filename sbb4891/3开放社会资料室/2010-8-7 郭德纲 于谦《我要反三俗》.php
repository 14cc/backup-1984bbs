于：蛋糕啊是怎么着？ <br />
郭：我喜欢吃蛋糕。 <br />
于：为什么？ <br />
郭：我要借吃蛋糕的机会批评教育她。 <br />
于：没听说过都。 <br />
郭：给我发了一个短信：明天下午两点，我在天桥等你。你听听她选择这个地方。 <br />
