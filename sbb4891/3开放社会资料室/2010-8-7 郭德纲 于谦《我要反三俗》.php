于：也是批判。 <br />
郭：我是为了反三俗嘛。这儿坐着呢，手机来短信了：很想和你花前月下一起散步。我这个火儿腾腾的就上来了。 <br />
于：没准儿是你爱人。 <br />
郭：呸~~~我媳妇儿不认字儿。 <br />
于：那就是情人。 <br />
郭：我弄死你啊。 <br />
于：怎么了，怎么了？ <br />
郭：我是一个玉洁冰清的人。贞烈贤良就是我的代名词。我走到哪儿贞节牌坊就跟到哪儿，我绝不做外活儿知道吗？ <br />
于：什么玩意儿。 <br />
郭：我儿子都上初一了我能那样做吗我？恨得我没法儿没法儿的。 <br />
于：是吗。 <br />
郭：想和我花前月下一起散步，太色情了。 <br />
于：您要不琢磨就没什么色情的。 <br />
郭：越琢磨越色情。 <br />
于：都是你琢磨出来的。 <br />
郭：散步之后就是吃饭，吃完饭就是回家睡觉，太三俗了！我不能饶了她。 <br />
于：对。 <br />
郭：(发短信状)你是谁？ <br />
于：您还问什么？ <br />
郭：我得知道她是谁我好教育她呀。 <br />
于：用得着你教育嘛？ <br />
郭：我这是苦口“破”心。 <br />
于：太破了。 <br />
郭：单位工作也很多，我还忙活这个事儿？我得问清楚是谁？ <br />
于：啊。 <br />
郭：王秘书过来，我得批评你。我说过是一次了吗？装订文件绝对不能超过十页！你看这二十多篇儿了，下次局长再撕不动我就抽你。 <br />
于：局长撕文件啊？ <br />
郭：这是谁呢？ <br />
于：还琢磨呢。 <br />
郭：有时候人一忙起来就忘记了自己的存在，到这会儿我掏出钱包来，掏出我太太的照片。回想那些草长莺飞的日子，每到这个时候我就自己劝自己：整个天下还有比她难对付的事儿吗？ <br />
