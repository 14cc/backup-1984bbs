于：干嘛一提这个您上弦了怎么着。 <br />
郭：很三俗嘛。 <br />
于：没忘，又想起来了。 <br />
郭：你那个手机接到过吗？ <br />
于：谁也保不齐接到过。 <br />
郭：看了吗？ <br />
于：发过来怎么也得看。 <br />
郭：给我讲两条。 <br />
于：我不传播这个知道吗。 <br />
郭：太三俗了。你竟然看，是人吗你？你堕落了，你堕落了你现在记住了，你需要有人带领你走出泥潭。你进一步就是立即枪毙，回头一步就是保外就医。 <br />
于：啊，我还好得了好不了了？不是，您收到过没有？ <br />
郭：那还少得了吗？一些个无聊的人发给我的。 <br />
于：那么你看不看呢？ <br />
郭：当然啦。 <br />
于：当然什么意思？ <br />
郭：我要看它无聊到什么地步。 <br />
于：您也看。 <br />
