于：你现在就跟外星人差不多。 <br />
郭：讨厌，你怎么这样呢？我一直在考虑，到底是谁呢？ <br />
于：你有点儿别的事儿没有啊？ <br />
郭：你怎么回事儿？ <br />
于：废话，你考虑这个干嘛。 <br />
