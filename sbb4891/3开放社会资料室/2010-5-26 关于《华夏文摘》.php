　　《华夏文摘》杂志每周星期五出版<br />
――――――――――――――――――――――――――――――――<br />
　　〖编者按：这篇文章摘自一个在海外信息领域工作的中国学者于2003年夏天为一个在湖南长沙召开的华人网络研讨会写的论文。原文题目为：“跨界・拓边・新空间――华人网络与互联网上的中文媒体实践”，其中有关CND部分是作者对本刊采访和阅读大量CND出版物之后写成。由于文章是在中国发表，本文没有提及或者从简从略了许多ＣＮＤ历史上的重大事件和里程碑。〗<br />
<br />
<br />
　　　　　　中国新闻电脑网络（China News Digest）――ＣＮＤ<br />
<br />
<br />
　　有著十四年历史的ＣＮＤ，是一个从来没有物理性的办公室，也没有可以面对面的编辑部，而完全在虚拟的电脑空间中诞生、成长、存在的媒体。<br />
<br />
　　从1980年代后期开始，前往北美、欧洲留学的中国大陆学生逐渐增多。在那个时期，海外各地的中文媒体并不发达，因此对于远离故土的留学生，特别是那些分散在大城市以外地区的人来说，想要获得有关中国的消息，并不是一件容易的事情。当时，虽然万维网（World Wide Web）还未诞生，但世界范围的电脑联网已经成为欧美各国的大学、政府、企业之间最重要的信息传输手段之一。一些中国留学生也开始在被称作“新闻组”的网上论坛上用英文进行交流。<br />
<br />
　　1989年3月6日，四名留学美国和加拿大、在网上相识的理工科学生，利用大学里的电脑系统建立了中国新闻电脑网络（China News Digest），简称ＣＮＤ。ＣＮＤ每天通过电子邮单的形式，转发西方各大通讯社有关中国的英文报道，有时也编写一些有关签证、居留、回国等留学生非常关心的内容。ＣＮＤ发行第一期时，邮单上共有400人，而且大多在加拿大。但不到一个月，这份邮单上就扩大到两、三千人，遍及美国各地和欧洲。经过两年的发展完善，邮单上的读者人数已达到一万多人，来自二十多个国家和地区。于是，ＣＮＤ决定在此基础上建立一个全新的系统――《华夏文摘》中文网络。<br />
<br />
　　《华夏文摘》于1991年4月5日正式创刊，作为世界上第一家中文电子刊物而被写进历史。《华夏文摘》每周星期五出版，通过互联网免费传送给全球各地的订户，十二年来从未间断或迟延过。1990年代初，很多地方的电脑没有中文软件，无法接收中文文件。即使在可以接收的地方，中文电脑通信也要比常规的英文电脑通信复杂许多。但是，《华夏文摘》在渴望中文资讯的留学生中迅速扩大了影响，成为大家每周期盼的“精神食粮”。<br />
<br />
　　1993年，曾有读者如此描述其第一次在电脑上读到《华夏文摘》时的心情：“还记得一年前的一个星期天，当我第一次成功地在我那一向只会‘讲’英语的电脑屏幕上读到《华夏文摘》时，兴奋得手舞足蹈，高呼‘万岁’的情形。来美五年了，一直生活、学习在美国中西部的一座没有‘唐人街’的城市中，每天学的、读的、听的、讲的、写的都是清一色的英文。此时，屏幕上扑面而来的一行行整齐、漂亮的仿宋体汉字是那样地熟悉，那样地亲切！”许多读者还将《华夏文摘》保存到软盘里，或是打印成册，转给那些无法在电脑上阅读中文或是不会使用电脑的家人和朋友们看。<br />
<br />
　　ＣＮＤ的编辑和运营人员都是志愿工作者。《华夏文摘》创刊初期，大部分文稿取自海内外各家中文刊物，小部分选自读者投稿，是一份名副其实的文摘型杂志。当时能在网上找到的中文稿件极为有限，因此将印刷媒体上的文章打字输入电脑，成为整个编辑过程中最为繁重的工作，需要大量的人手。ＣＮＤ经常在《华夏文摘》上刊登征募志愿工作者的启事，鼓励读者参加编辑、校对、读者技术咨询、网络技术维护、打字、主编等各项义务工作。ＣＮＤ创办以来，曾有数以百计居住在世界各地的读者或长或短地加入过义务编辑工作的行列。绝大多数工作人员相互间从未谋面，也没有通过电话，但却通过互联网相识，在不同的时区，从各自学校的机房或是家里或是上下班的电车上，为编辑同一期杂志而工作。1993年2月《华夏文摘》出满100期的时候，读者们得知ＣＮＤ至今没有一台专用电脑，于是在网上自发为ＣＮＤ募捐。在一个月之内，共有四大洲十几个国家的700多名读者捐款，使ＣＮＤ首次拥有了自己的联网电脑。<br />
<br />
　　ＣＮＤ在以中国大陆留学生为主体的编者和读者的共同努力下发展壮大，也与编者和读者们一起成长，并没有一直停留在留学生刊物这个位置上。进入1990年代中期以后，随著1980年代的留学生大多结束学业并已在各国安家立业，同时配合互联网环境和技术的发展，ＣＮＤ从内容构成到编辑体质都进行了一些明显的调整。<br />
<br />
　　首先，《华夏文摘》已经从最初的偏重文摘转变成为主要发表原创作品的杂志。另外，ＣＮＤ在很长一段时间里，为了照顾无法阅读中文的读者，发行了ＣＮＤ全球版、美国版、加拿大版、欧洲／大洋洲版、China Internet News letter等数个英文出版物，但目前英文出版物仅保留了ＣＮＤ全球新闻。<br />
<br />
　　将网站的服务器从大学等教育设施中移出，也是ＣＮＤ告别校园的一个标志。1998年以后，ＣＮＤ不再免费使用大学里的服务器。目前共有四个服务器设置在美国和加拿大不同的地方，委托专门的公司或专人管理。<br />
<br />
　　电子文本的普及大大减轻了编辑工作的负担，已不再需要投入大量人力来打字输入文章。而ＣＮＤ网站内容的扩充，更新速度加快；以及互联网技术水平的提高，也要求编辑之间保持更迅速的互动和更默契的合作。因此从1996年以后，ＣＮＤ逐步减少编辑过程中对义务工作者的依赖。现在，ＣＮＤ所有的编辑业务都由一个以核心成员为主，配合多年的工作小组来完成。<br />
<br />
　　1994年6月，ＣＮＤ将诞生不久的World Wide Web创译为“万维网”，并在万维网上开设网站，每周星期五将《华夏文摘》发送给邮单订户的同时上网。从此，不是邮单上的订户，也可以直接从万维网上读到《华夏文摘》等ＣＮＤ出版物，使得读者数量大增。<br />
<br />
　　2000年，ＣＮＤ对网站进行了大幅度改版，新设了《华夏快递》、《华夏文库》和自由论坛，再次扩大了读者群。《华夏快递》每天推出十余篇来自读者的投稿和文摘；《华夏文库》收集了ＣＮＤ过去十多年来300多位作者和35个专栏的文章；自由论坛中最为活跃的是《华夏论坛》、《读者评论》、《华夏天地》这三个讨论区，任何人注册之后均可自贴文章或发表意见。特别是在《读者评论》中，读者们对《华夏文摘》、《华夏快递》上的文章发表评论；而《华夏文摘》、《华夏快递》上的作者也经常在论坛里和读者交换意见，形成互动。ＣＮＤ网站还设有资料信息库，其中分类保存了有关文革、八九学运、南京大屠杀、中国近代史等历史性的文字和图片资料。而从网站上的《每日新闻摘要》和《国际简讯》上，读者能获取最新的中国国内新闻和国际新闻。<br />
<br />
　　ＣＮＤ网站的主页上，除了一些旗标（Banner）广告之外，基本上是白地蓝字，没有过多的色彩和图片。页面中央的最上端，是最新消息栏，接下来按照时间顺序排列著《华夏快递》最新文章50篇和读者讨论区热门话题100条。页面的左右两边，则罗列有ＣＮＤ各个出版物、讨论区、读者服务项目、信息资料库一览，以及将读者点击率高的文章汇聚一堂的《华夏快递》特别推荐文章的标题。整个页面将平面设计的视觉要素压缩到最低限度，好像一张铺开的目录索引，使读者能从众多的条目之中尽快发现并进入自己感兴趣的栏目中去。<br />
<br />
　　十几年来，虽然互联网几经潮涨潮落，ＣＮＤ一直保持著朴素的门脸，也一如既往地以非营利组织的身份，免费为读者提供新闻及出版服务。ＣＮＤ上的内容涉及范围极广，观点也不尽相同。但ＣＮＤ在创刊号中提出的“注重新闻性、趣味性、知识性和资料性”的选稿原则一直坚持到今天，这使得各种类型的读者都能从ＣＮＤ上找到符合自己口味的内容，也是ＣＮＤ长期拥有庞大而稳定的读者群的原因。除去邮单订户这批固定读者，越来越多的人直接在网上阅读ＣＮＤ的出版物。其中，《华夏快递》、《华夏论坛》、《华夏文库》每天都拥有数万名读者，而每到星期五，更有几十万人从世界各地访问ＣＮＤ网站。<br />
<br />
<br />
