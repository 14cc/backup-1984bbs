													<h2>美国：我们的故事</h2>
						
						
																											<div id="postmessage_746304" class="t_msgfont">中文名: 历史频道 美国：我们的故事<br />
英文名: History Channel America: The Story of US<br />
片子简介：<br />
<br />
2010年4月，America: The Story of US「美国：我们的故事」在美国的历史频道首映。这是一部12集的史诗纪录片，收视率超过了绝大部分美国虚构电视剧。它全面使用电脑CG，把阿凡达式的视觉效果和好莱坞式的表演技巧引入纪录片，令人目瞪口呆，忘记这是真实的历史。<br />
<br />
第一集的第一组镜头就把人牢牢吸引住了。深蓝色的水底，一条大鱼游过，随后从水下看到一艘船缓缓驶来。在一个俯瞰船身的镜头之后，摄像机加速度向着美洲大陆飞行，镜头前领飞的是一只美国兀鹰。<br />
<br />
这部纪录片的叙事采取了画外音叙述，演员表演，嘉宾评论三种历史纪录片常用的方式，并把这三者巧妙地穿插在一起。给演员的镜头频繁使用特写，过肩，偷拍等纪实风格，与现代影像语汇契合，同时又跟历史虚构片保持了距离。<br />
<br />
CG在片子中运用得出神入化，镜头时常拉出事发地，一直拉到俯瞰地球为止。在前两集中还多次使用了黑客帝国曾经用过的子弹时间特效，以表现军事冲突的紧张与暴力美学。<br />
<br />
<br />
<br />
我认为片中与内容最相得益彰的特效是表现本杰明-富兰克林骑马送信的镜头，他和马匹变成了一个火球，在13个殖民地上滚动，随后火球蔓延开去。画外音解说：在殖民地，信息自由流动，英国军队开枪的暴行很快传开。而在英国好多人还不知道。<br />
<br />
看了一些美国的评论，大家普遍认为，这片子史实还比较牢靠，反映了面向美国中小学生的主流史观。编创者不忘政治上正确，强调美国革命队伍中也有黑人。嘉宾的访谈评论点到为止，对欣赏画面几乎不构成干扰。尽管如此，美国还是有电视人对于使用嘉宾访谈形式提出了批评。有的地方我也觉得别扭。例如请出一个现役海军陆战队上校，说我恨不能跟华盛顿一起并肩作战。口味有点太重了。想像一下，一部讲述岳飞的纪录片，请一个解放军军官对着镜头说：我希望我能投入抗击金兀术的战斗中……<br />
<br />
总体来说，我给美国:我们的故事打五星。看过它之后，我立即找出一本目前最火的Remini写的「美国简史」对照阅读，果然印象深刻，趣味盎然。这也是这部大制作纪录片的初衷，希望把人们（主要是年轻人）吸引到瑰丽的历史王国里来。从目前它的受欢迎程度来看，这一目标应该不难达成。（转自<a href="http://blog.sina.com.cn/s/blog_6237b7510100i09m.html" target="_blank">http://blog.sina.com.cn/s/blog_6237b7510100i09m.html</a>）<br />
