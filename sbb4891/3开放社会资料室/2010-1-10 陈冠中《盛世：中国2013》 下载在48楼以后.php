													<h2>“盛世”何须危言2010-1-15 9:56:04　来源: 南都周刊</h2>
						
						
																											<div id="postmessage_436661" class="t_msgfont"><img src="http://www.nbweekly.com/UserFiles/fckfiles/2010/01/13/40e3b5ed44bc4f60aca4f18154410ba0.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
“小说是需要严肃对待的事情”。为了写小说，香港文化人陈冠中搬到北京，旅居十年，他终于写成了其第一本以中国为背景的小说《盛世》。从香港到北京，陈冠中尝试着转换身份，转换观察视角，而他对大陆诸多现象和问题的思考，终于在这本看似魔幻的《盛世》中，一一写尽。南都周刊记者_孙骁骥 摄影_邵欣<br />
 <br />
　　2013年，世界经济进入“冰火期”，中国盛世正式随之开始。那时，旺旺集团收购了星巴克，中式产品已经远销世界各国。每个中国人的脸上都洋溢着幸福。在河南一个叫快乐谷的地方，兴建了制造幸福药剂的工厂，药剂通过自来水进入全国人民的日常生活。但实际上，在进入“盛世”之前，有整整一个月的时间不见了,这个月究竟发生了什么，已经没有人记得。一位叫芳草地的归国华侨、前国家法院的法官小希、作家老陈，总是感觉有些什么不对劲，要追问那丢失的一个月。<br />
 <br />
　　这是陈冠中在《盛世》里讲述的故事。这本书虽然尚未在大陆发行，但却在知识分子圈子里口口相传，并通过微薄等社交工具的传播效应，开始被越来越多的人关注。<br />
 <br />
　　陈冠中是谁<br />
 <br />
　　在香港文化圈，陈冠中的名字无人不知，他曾经创办了香港知名杂志《号外》，并和梁文道、林奕华等创立“牛棚书院”。可在大陆，他却要时时面对“陈冠中是谁”的提问。对此，他说，“用香港作家称呼我是最简单的”。<br />
 <br />
　　生于上海，长于香港，最后定居北京。陈冠中将之视为一个自然发生的过程，他说香港让他找到身份认同，而北京，则是他观察中国的中心舞台。<br />
 <br />
　　1992年，陈冠中带着投资意向来到大陆。当时，北京的文化圈人士还比较穷，陈冠中每次请客吃饭都担心对方回请时破财。于是，他在亮马公寓租了一间大房子，请来做饭的阿姨摆开“流水席”，宴请四方宾客，从王朔到王蒙，都成为陈冠中的座上客。<br />
 <br />
　　事实上，这并不是陈冠中第一次和大陆的文化界发生关系，他曾做过《读书》杂志的海外出版人，并投资了《现代人报》、《三联生活周刊》以及“大地唱片”等，当年艾敬唱红大江南北的“我的1997”，就是陈冠中的杰作之一。在上世纪八九十年代，陈冠中在大陆的投资风生水起，领域横跨影视、音乐、出版产业。<br />
 <br />
　　从1997年开始，陈冠中突然想“好好写东西”。这一写就是十年。为此他在2000年搬到北京，并选择了更安静的生活方式。平时除了见见老朋友，参加一些活动，其余的时间，大多用来观察、思考和撰述。“写作的时候我内心是很严肃的”。十年间，陈冠中的头发由黑色转为灰白，诚是岁月不饶人，但这漫长的时间，足够让陈冠中观察中国社会的方方面面。<br />
 <br />
　　“绛树两歌”谈中国<br />
 <br />
　　在北京，《盛世》基本成为了知识圈里的一本读物。陈冠中无意于将它在这里出版。但他并不担心读者们无法看到此书，“据说淘宝网上就有卖的”，他知道，对于圈内人士来说，通过一定的渠道得到此书其实不难。<br />
 <br />
　　看到此书不难，但是要真正把书写透，很难。“中国太大了，很多事情无法三言两语说清”，令陈冠中反感的是那些将中国问题简单化的看法，比如流行一时的“通三统”理论。陈冠中很纳闷，为什么一些内地学者热衷于这种简单的思维方式。<br />
 <br />
　　陈冠中认为，要想说清楚中国的问题，前提是要学会“绛树两歌”。绛树是指古代一位歌女的名字，能用一张嘴同时唱两支歌，“一声在喉，一声在鼻”。他2005年在台湾《思想》杂志发表的文章就起名为《顾左右言他：中国论述的绛树两歌》。<br />
 <br />
　　陈冠中引述实证主义思想家韦伯和波兰尼的理论，认为应该摒弃那些过于绝对、简单的思维方式，只有以“复杂的鸡尾酒式全球观”，才可能比较真实地观察当今中国。<br />
 <br />
　　但陈冠中的“绛树两歌”起初也让人有些疑惑，究竟作者的褒贬态度如何？著名学者李欧梵在谈到陈冠中和《盛世》时不禁问：“这个《盛世》描写的是一个‘乌托邦’的中国还是一个‘反乌托邦’的中国？”他最初也把此书当做幻想小说来读，可越读下去越发现文字充满了“现实”的意味。<br />
 <br />
　　陈冠中似乎具有一种超然于世外的角度，既作为当代中国的观察者，又置身于事外。不仅是陈冠中个人的态度，也是香港文化人的共识。陈冠中出国留学、学成归国的七十年代，正是香港各种思想并举的年代，欧洲、特别是法国的新浪潮电影进入香港，各类城市杂志不断涌现，社会上标榜“左”或者“右”的团体也并存不悖，竞相争鸣。<br />
 <br />
　　文化人梁文道说，香港人历来对于过分表达自我有一种厌恶，这一点和内地流行的宏大叙事、台湾流行的悲情主义非常不同。香港知识界在70年代就开始嘲笑文学作品中的文艺腔，以比较“冷漠”的态度写作，是香港作家的一个特点。他们不会把自己的立场大声喊出来。<br />
 <br />
　　在陈冠中看来，盛世不一定非得“危言”。“批判也可以是一种疑问、一种描述，小说不是非此即彼的论述。”他不过是在中国体会到些什么，并把它写成了一个故事。<br />
 <br />
　　陈冠中×南都周刊：人是我留在北京的唯一原因<br />
 <br />
　　2013是一本现实小说<br />
 <br />
　　南都周刊：《盛世》动笔之前有没有设想过这本书的读者群？<br />
 <br />
　　陈冠中：对我来说，这本书有两个主要的读者群。一个就是香港、台湾的读者群。就这个读者群来讲我一直认为他们对中国的认知是有距离的，有鸿沟的。我希望能够把他们带到一个比较接近中国现实的状态。如果读者看了书，认为：“原来中国是这个样子的”，那我就很满足了。这是我对港台读者的期待。<br />
 <br />
　　另一个是内地读者群。这本书10月在香港出版，12月在台湾出版，如果内地读者能通过他们的渠道买到这本书，我期待他们觉得挺靠谱的。内地这么纷乱、难以言说的现状，有人用文本把它表现出来，不管它完整与否，起码做过这样一种努力。中国的现实其实比小说更魔幻，很难说清楚。只有小说语言勉强有机会把它说清。<br />
 <br />
　　南都周刊：你这几年也撰写过一些关于内地现状的文章，这次是怎样想到小说这个表达方式的？<br />
 <br />
　　陈冠中：人对于现状的情感和体会，有时候如果不用小说表达会很难。但文化圈有很多人不看小说的，我甚至拿了书都不敢送给别人。实际上小说经过几百年的发展，到了今天可以说是百无禁忌。它有很大的自由度。我自己尝试用小说之外的文章去写内地，但都不是太满意，很难完整地把内地的感觉写出来。我发现还是要用小说来写比较合适。<br />
 <br />
　　南都周刊：这本小说用了多久时间完成？<br />
 <br />
　　陈冠中：其实2000年我到北京就想写一篇关于中国的小说，但想了几个题材之后发现自己写不下去，毕竟自己的童年、青少年都不在内地，所以没有什么底气，也找不到好的题材。直到2008年，从地震到奥运，那个时间我才看到一个小说的架构。所以一过2008年我就开动笔始写，不到半年就很快写完了，中间还停顿、修改了几次。<br />
 <br />
　　70年代的香港更有趣<br />
 <br />
　　南都周刊：你这一代香港人是否都怀念文化上的70年代？<br />
 <br />
　　陈冠中：香港1950年后出生的人数是最多的，1949年不少人从内地迁过去，造成了“婴儿潮”的一代。1945年的时候，香港才60万人，50年代初就有一两百万人，忽然人口猛增。那一代人刚好在70年代长大，取得了一些话语权，这帮人人数多，而且到现在还占据着社会的关键位置，所以他们整天觉得自己好重要。但对于之后的下一代来说，70年代不见得是多么重要的年代。<br />
 <br />
　　其实香港的黄金时代是80年代，但黄金时代的成功是因为70年代有很多新东西雨后春笋一样冒出来，比如在电影领域就有很多不同的尝试。但80年代商业片的成熟带来的结果是很多小型的片就没有了，大家一心想着拍商业片赚钱，这有点像内地这些年的情况。<br />
 <br />
　　南都周刊：你会有意无意对比内地和香港的情况吗？<br />
 <br />
　　陈冠中：以前两边确实是有差距的。比如2000年回来的时候，当时要拍一个时装大片，全内地只有30个摄影师有能力去掌握时装大片，很多连机器都没有，2004年之后，这样的差距非常小。以前商业片也是差距很大，90年代很多电影制作人找化妆、道具、动作设计的时候都会想到香港，但现在两边很多方面可以互相替代，内地很多方面甚至超过香港，现在内地的好莱坞就在北京。<br />
 <br />
　　南都周刊：为什么选择北京作为观察中国的视角？<br />
 <br />
　　陈冠中：人是我呆在这里的唯一原因。我也没有其他选择，台湾、香港也有不少人才，但人才的集中是没法和北京比较的。我只能呆在一个地方，中国真的太大了，只有北京可以相对完整地理解中国。比如上海，上海的人是不太谈论中国的，他们只谈上海和国外，香港也谈不上理解中国，而北京，饭桌上大家都在谈论全国的事情。<br />
 <br />
　　南都周刊：对于香港未来的文化走向，你有什么期待？<br />
 <br />
　　陈冠中：香港人有一个误区，就是认为一定要跟北京合起来。其实它可以和广州、深圳这些城市合起来，形成一个粤派文化圈，肯定影响力会超过上海。有一次我在深圳演讲，有很多深圳的年轻人跟我说，很想自称为深圳人，想建立自己的文化。其实深圳和香港首先要连起来。文化界要建立一个新的心理地图，文化产业需要民间自己来搞，官员是搞不好文化的。</div>

							
							
							
							
