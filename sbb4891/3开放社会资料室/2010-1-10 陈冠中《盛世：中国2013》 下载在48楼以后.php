																											<div id="postmessage_434378" class="t_msgfont">《盛世：中国，2013年》 [陈贯中/著]<br />
<a href="http://bbs.ourtw.com/thread-13191-1-1.html" target="_blank">http://bbs.ourtw.com/thread-13191-1-1.html</a><br />
<br />
这部小说里面的大题目很多，值得用十万字、几十万字来讨论。这里暂且搁置。单以小说论，《盛世》也有不少有意思的细节。琢磨小说，顺便琢磨作者，我读出两点或许无关宏旨的心得。 <br />
&nbsp; &nbsp;<br />
牛津大学出版社（中国）有限公司 2009年10月 <br />
<br />
&nbsp; &nbsp; 陈冠中的《盛世》，可以戴上很多帽子：反面乌托邦小说，政治寓言小说，科幻小说……这些帽子有可能合适，有可能不合适，即便合适，别人都戴过，顶在作者飘逸的长发上，并不为他增色。说得实在点，这是一本难得的知识分子小说，知识分子写，知识分子看，知识分子议。说它难得，因为这些年真正有知识含量、智慧含量的知识分子作品太少了。对这类作品的期待近于绝望的时候，忽然读到了陈冠中的小说，那种爽快的感觉，如同喝了多年星巴克旺旺合资产品“桂圆龙井拿铁”后，又一次尝到纯粹的Espresso。再晚一步，我们对智慧产品的赏味能力会退化。 <br />
<br />
&nbsp; &nbsp; 这部小说里面的大题目很多，值得用十万字、几十万字来讨论。这里暂且搁置。单以小说论，《盛世》也有不少有意思的细节。琢磨小说，顺便琢磨作者，我读出两点或许无关宏旨的心得。 <br />
<br />
&nbsp; &nbsp; 我发现作者相当讨厌当下八五后、九○后的得志青年。许多小说写两代人的矛盾，一般作者通常都虚伪、虚弱地去拥抱年轻。陈冠中却反写代际冲突，毫不掩饰他对盛气凌人、不知轻重的年轻人的反感。 <br />
<br />
&nbsp; &nbsp; 《盛世》中各类人物的面目都有点模糊，但没有绝对意义上的恶人。如果有，只有一人，二十四岁的韦国。他聪明、上进、名牌高校的大学生、手眼通天，“不是书呆子，读的是政法思想和经世资治之学，座右铭是智勇双全――提倡尚武精神、英雄主义和男子汉气概。是有使命感的精英，在平庸而没有荣誉感的年代，有勇气承认自己是中国盛世的真正精神贵族”。他挑了六个崇拜自己的大学生练武，训练他们的胆色杀气，去野营，杀流浪狗。他迫害自己的亲生母亲。他最浪漫的职业理想是做精神公务员。 <br />
<br />
&nbsp; &nbsp; 我猜作者真的见识过韦国型的孩子。但见过是一回事，略带偏憎地创作这样一个人物是另一回事。刻薄如鲁迅，遇见年轻人还要礼让三先；狂妄如李敖，最多自不量力地和年轻人比身体。能鄙薄新进，表明作者并不在乎浅薄的进化论。后来人并没有道德上的特权。最新的也许是最糟糕的。 <br />
<br />
&nbsp; &nbsp; 我很喜欢作者面对年轻一代的孤高之气。他写老陈参加《读书》的新春茶聚，只和“老一辈的编辑、作者瞎聊几句”，“至于年轻编辑、作者就算了，我不认识他们，他们也不觉得有必要认识我”。 <br />
<br />
&nbsp; &nbsp; 《盛世》的主要人物是老陈。老陈“香港出生，在调景岭念完小学，追随父母迁居台湾”。出场时是台湾“名记者、小说家、大陆问题专家”，他迁居北京多年，完全融入北京的文化圈。老陈是不是陈冠中，读者不得而知。小说中的老陈差不多已经是地道的北京人，毫无港台文人对大陆一半虚骄、一半惶恐的做作，他所作所为所思所想是标准的大陆知识精英配置。 <br />
<br />
&nbsp; &nbsp; 老陈热爱侦探小说，我也喜欢。厉害的侦探会在常人不留意的蛛丝马迹上有所发明。我侦查《盛世》，比较得意的发现是：其实老陈只是和北京人民打成一片，骨子里还是海外文化圈的“一个人物”，对大陆朋友有点瞧不上。证据是老陈、简霖、何东生的酒聚。 <br />
<br />
&nbsp; &nbsp; 那几场酒聚也是目前北京新富圈法国红酒热的一个缩影。地产商简霖“拿出一瓶好酒，八二、八五、八九的一线波尔多，两个人喝，有时候一晚喝上两瓶”。老陈此时的内心独白就非常港台：“台湾人喝好的红酒，比大陆早上十五年，我能附和他，欣赏他的酒，也愿意听他卖弄书刊里看回来的酒经”。最后一句很准确，也很恶毒。新富对自己的感官享受没有把握，必须从书刊里找根据。老陈还对简霖的红酒文化学习做了点辅导，指点他不能波尔多一味独沽，还要喝点勃艮第。 <br />
<br />
&nbsp; &nbsp; 我觉得好玩的是，简霖固然是半瓶子醋，老陈何尝不是五十步笑百步。红酒文化哪里真有那么多的玄机。按照阴谋论的思路，所谓红酒文化，不过是法国酒商和政府花了几十年的时间联手打造的推销广告。现在法国人自己都不相信。法国最近的商业调查显示，大多数法国人认为红酒是有害食品，危险性仅次于猪肉。红酒文化，蒙的就是大中华，先下香港，再占台湾，最后大陆。 <br />
<br />
&nbsp; &nbsp; 我庆幸吹毛得疵，终于发现老陈的一个小纰漏。否则《盛世》也太彪悍了。<br />
