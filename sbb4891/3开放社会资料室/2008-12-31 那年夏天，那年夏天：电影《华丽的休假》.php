																											<div id="postmessage_15904" class="t_msgfont">那年夏天，那年夏天：电影《华丽的休假》<br />
<br />
王怡<br />
<a href="http://www.artblog.cn/U/joshuawang/archives/2008/38752.html" target="_blank">http://www.artblog.cn/U/joshuawang/archives/2008/38752.html</a><br />
<br />
<img src="http://www.artblog.cn/UploadFiles/2008-1/101029427142.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
也许我不该在新年看这部影片，不该让我的屏幕上充满屠杀。<br />
<br />
我却不能不看。韩国人等它等了27年，我也等了18年。有人说韩剧很轻松，其实也很严重。韩国联合通讯社引用一个叫吴京燮的逃亡者的证词，说在北方，有人因偷看韩剧而被枪决。韩国导演花了多少年，步步为营，试着描述当年的光州惨案。每个亚细亚的孤儿都有一个创口，黄色的脸庞，有红色的污泥；黑色的眼珠，有白色的恐惧。就像在台湾，人们花了40年，才能公开纪念二二八事件。<br />
<br />
先是2001年，一部《爱的色放》，将一个偷情与弃婴的故事，令人突兀地放在1980年光州惨案的背景下，肉体的纠缠，就与时代的恐怖；叫声也与枪声此起彼伏，当时我心中寒意四起，难道先民主起来的韩国人，就这样来投射他们对一场屠杀的记忆吗？<br />
<br />
到2006年，韩国电影的光州情结，开始从色情走向纯情。一部《那年夏天》，将一座向着平民开枪的光州，铺陈为一个恋爱与追忆的舞台。在光州事件的图片中，最令我痛得叫唤的，是市民们连夜赶制国旗的场面。他们拿起枪与全斗焕的戒严部队作战，然后把一面面国旗覆盖在死难者身上。电影中，一位教授寻找着1969年的初恋情人；而1980年的光州，何尝不也在寻找一个丢失了的大韩民国的愿景。<br />
<img src="http://www.artblog.cn/UploadFiles/2008-1/101030561889.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" />《那年夏天》<br />
<br />
到2007年，先后出现两部描写光州民运的电影。《古老的庭院》，一个被判死刑的政治犯的爱情，大学生与政府军街头对峙的场面，令人想起尚可背诵几句的林觉民《与妻书》：<br />
<br />
意映卿卿如晤：吾今以此书与汝永别矣！……吾至爱汝！即此爱汝一念，使吾勇于就死也！<br />
<br />
<img src="http://www.artblog.cn/UploadFiles/2008-1/101030328539.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" />《古老的庭院》<br />
<br />
可惜影片水准不够。直到最近的《华丽的休假》，正面重现了“518”惨案前后10天的光州。电影作为一种民族记忆的形式，终于向着昨天鞠躬致敬。1980年初，军人全斗焕在朴正熙遇刺后，与卢泰愚发动政变。金大中、金泳三等人领导民主抗议浪潮，提出宪改方案。除了汉城，全罗道的首府光州成了第二个民主运动中心。政府派出特种部队进驻大学，封锁了光州。<br />
<br />
<img src="http://www.artblog.cn/UploadFiles/2008-1/101032893086.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" />光州事件：历史图片1<br />
<br />
影片开头，各人美丽的人生，细细展开，对未来浑然不觉。而光州的上空，特种部队开始盘旋。一位士兵望着机外，发现并不是去北方，他诧异地对同袍说，“我们去的是南方，因为太阳是从左边出来”。<br />
<br />
出租车司机民宇，拉扯大一心想考法学院的弟弟振宇。他爱上了和弟弟同在一间教会的护士新爱。5月18日，民宇拉上弟弟当陪衬，和新爱去看电影。戒严部队冲入影院，开始了街头镇压。休假变成休克；华丽的夏天，成了韩国史上一个盛开的伤口。<br />
<br />
影片相当程度上还原了当时的记载。当天部队第一次向聚集在天主教堂外的大学生开枪。片中一个被捉拿的市民叫喊说，“我不是大学生”。20日，20万光州市民走上街头，振宇和他的同学也想去，老师们手拉手拦在校门口，劝他们爱惜生命。这一天军队开火，当场枪杀54人。翌日，30万抗议民众再上街头。振宇对老师说，不要拦我们。老师掏出一管膏药，说我知道拦不住，把这个涂在眼皮下，可以防止催泪瓦斯。这天还有一个新闻史上著名的场景。一位青年站在一辆坦克上挥动国旗，呼喊“光州万岁”，人群一起唱起国歌。而军队当众枪杀了这名青年。 <br />
<br />
<img src="http://www.artblog.cn/UploadFiles/2008-1/101029833965.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" />光州事件：历史图片2<br />
<br />
几次的街头对峙，影片的处理颇为出彩。尤其是突如其来的屠杀之前，人们以为军队将要撤离。就如当年在布拉格街头，捷克姑娘曾以放浪的飞吻和掀起裙子的动作，向苏联士兵示威。几个前面的光州市民，也以挑逗性的玩笑向特种部队示威，直到叫嚷“你们看他的裤裆都满起来了”。这时国歌奏起，士兵们提枪致敬，所有戏谑的示威者也顿时庄严起来，手按胸口。不料军队却变换队形，开始射击。<br />
<br />
<img src="http://www.artblog.cn/UploadFiles/2008-1/101031103924.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" />光州事件：历史图片3<br />
<br />
振宇死了。他留下一封信，把哥哥给他买吉他的钱，买了新爱最喜欢的十字架项链。他说，虽然希望有一把吉他，但更希望新爱成为他的嫂嫂。新爱的父亲是特种部队的退役校官，他和民宇一道，开始率众抢夺武器，组织民兵。直到27日，全斗焕的几千坦克碾过民众的躯体，进入市中心。镇压了最后一批守在市政厅主楼的抵抗者。电影中，民宇和新爱的父亲都死在了主楼的抵抗战中。<br />
<br />
<img src="http://www.artblog.cn/UploadFiles/2008-1/101029447137.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" />光州事件：历史图片4<br />
<br />
要紧的是，他们到底在做什么？新爱的父亲组织民兵时，宣称“向老百姓开枪的军队，才是真正的叛军”。而裴将军却起身给了他部下一个耳光。部下问，我们怎么辨别叛乱分子和无辜市民。将军说混账，藐视国家军队的，哪里有什么无辜市民。振宇本来也不该死，他已举枪投降。但当一名军官反复说，放下武器，停止叛乱。振宇无法接受这一盖棺定论，他宁愿在乱枪中喊道，“我不是暴徒”。<br />
<br />
在光州惨案中，有一千多市民死亡或失踪，同年12月，全斗焕当选总统。新爱的父亲说，面对政府的叛乱，人民将行使最后的抵抗权。他是对的。 1987年汉城奥运会前夕，一百万人走上汉城街头，要求宪改。全斗焕被迫下台，政府接受宪改，结束了军政体制。1993年金泳三当选总统，1997年光州民主运动被“正名”。两位元凶、前总统全斗焕、卢泰愚，以“叛乱罪”被判重刑，后蒙赦免。2006年3月，韩国政府宣布，收回他们二人担任总统期间和卸任后获颁的所有勋章，其余参与光州事件的174位军人和政府官员，也被收回了勋章。<br />
