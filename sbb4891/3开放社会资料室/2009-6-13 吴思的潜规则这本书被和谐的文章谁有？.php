																											<div id="postmessage_124162" class="t_msgfont">造化的报应(2005-11-29 15:15:36)<br />
 &nbsp; &nbsp; &nbsp; &nbsp; 分类：精神现象<br />
　　一北京街头的十字路口有红绿灯，红绿灯下还有警察。通常还不是一个两个警察，而是四五个警察。这些警察都是要拿工资的，而这些工资来自税收，本来那是企业的利润，可以成为生产的动力和更多的就业机会。降低税收可以刺激生产，提高人们的生活水平，这是大家都明白的道理。那么在警察工资方面的开支能不能减少呢？美国的红绿灯下没有警察，人们仍然能遵守交通规则。人们对交通规则的遵守竟然可以到这种地步：明明没有车辆，一个人在人行横道的路口也会等待变灯。我在赫尔辛基就遇到过这样的事情。一位中年妇女在马路对面等待变灯，于是我也不好意思横穿空空荡荡的马路，一直等到红灯变绿。在中国，这当然是极其罕见的事情。这就是说，在美国和我说的那个欧洲国家，警察的工资是可以省下来的。省了下来，交通秩序并不会变坏。但是我确信，中国不可以节省这笔钱。至少在我非常熟悉的北京街头，红绿灯下的许多警察是绝对必要的。如果没有警察，只有红绿灯，我确信，这个路口就如同没有红绿灯一样，必定会拥挤得一塌糊涂。不用说别人，我本人也会跟着人们一块去挤。我经历过这样的事情，如果我单方面谦让，别人肯定不会让我，我永远也没有过路的机会。道德的约束本来就是非常脆弱的。如果有几个人不理睬红绿灯硬闯，这条路就要堵塞，我们这些打算遵守交通规则的人也就不能遵守，也没有必要遵守了。当然，警察的工资也不是绝对不可减少。譬如，四五个也可以减少到两个。只要在岗的人认真负责，应该可以维持交通规则的威严。如此威严久了，再将人降低到两个，最后减少到一个，也是可以的。但是这要求警察的工作量和责任心大大超过国家公务员的平均水平，似乎有些不合情理。<br />
　　我想由此说明的是，中国人自我约束意识弱，道德水准低，导致了政府的臃肿和税收的增加，导致了社会生活质量的下降。这原因不在别的地方，其实和我们每个人都有一些关系。我们活该。仅仅收入减少倒也罢了，我们非常在乎的国际面子也大受影响，我们的个人尊严因此蒙上了阴影。欧美的红绿灯无须警察压阵，这已经显出了人家的公德水平。德国的地铁竟不用验票，因为人人自觉买票，这更显出了人家的高超。而我们中国人在国外干什么呢？在日本打投币电话，竟然在中国人中流传着一种诀窍，用线拴住硬币，用完了再将其吊出来。这真是极其鲜明的对比。我的一个非常能干也很有教养的朋友在德国吃饭，他说自己遵守了一切礼仪，也按常规付了小费，但是仍然能清楚地感觉到侍者对他的轻蔑。他说，他知道这不是对他本人的侮辱，侍者看不起的是他所代表的民族。我们中国人自己也看不起自己，只要条件相同，宁可与外国人做生意，躲开自己的同胞，因为他们失信和欺骗的可能性更高。于是，我们的民族就遭到了报应，变成一个令人厌恶也彼此厌恶的民族。变成一个只讲利害关系，一有机会就坑蒙拐骗的人群。为什么会这样呢？为什么欧美国家的公德水平比我们高呢？为什么我们的人民不肯遵守明明对自己有利，谁都知道是非常必要的交通规则呢？按说，交通规则并不是什么复杂问题，并不需要多么高超的理性和信仰支撑。<br />
　　二我有一个猜测，可以部分解释为什么骑自行车的人缺乏自觉精神，非要警察监督不可。我承认一些人自制力差，像儿童一样需要父母监督。但这不足以解释大众的行为。毕竟在大街上走的多数是成年人，他们在工作单位里很能忍气吞声，自制力并不弱，他们所欠缺的似乎只是公德心。而公德心是对一种对自我与公共事务的关系的判断和肯定。<br />
　　中国老百姓认为自己是什么人？在与国家的关系方面，他们到底是什么？如果我们果真把自己当成了国家的主人，就像理论上应该的那样，我们交了税，雇人来管理交通，这些交通管理人员是我们的公仆，那么，我们会如何看待我们委托他们照应的这些事情？--我们大概会像一个主人那样，自觉小心地关照家里的秩序，并且提醒警察要认真负责地维护秩序，好好干活，不要偷懒。毕竟我们是给他开工资的。我们不会把公共交通当成那些官吏和衙役的事情，我们明白这是我们的事情。这使得我想到了一个朋友说到的故事。他说他在美国见过一件事情，印象很深。一个老头，看见路口堵车，很生气，就下车把维持交通的警察训斥了一顿。说我们交了税，应该得到好的服务，你怎么把交通弄成这样？警察毕恭毕敬地听他训斥。这在中国显然是不可思议的。至少对我来说，很习惯听警察的训斥，从来没有妄想过训斥警察。在我的体会里，中国的警察和司机的关系，属于典型的猫和老鼠的关系。老鼠--这就是中国百姓心目中的真实的自我形象。猫和老鼠--这就是真实的自己与公家的关系的真相。中国百姓很少有人当真认为自己是国家的主人。因为这不是事实。公共事务一直是皇上和官吏们垄断的事情，老百姓一直在躲避他们的惩罚、勒索和敲诈。在这方面，老百姓就是缺乏责任心，因为这确实不是百姓能够负责的领域。如果有谁不懂事，企图按照主人的方式行事，对了领导的心思则已，对不上就会碰得头破血流，海瑞和彭德怀就是证明。那些地位甚高的名臣尚且如此，更何况普通百姓。按照传统习惯，也按照我们对现实的认识，公共交通搞得好，这是领导有方，是皇上圣明和皇恩浩荡。我们要感谢政府把北京的交通整顿得那么好，让我们生活得那么方便。譬如出租车不许在长安街路边停靠，据说是为了方便管理。如果我们人民真把自己当成主人，这岂不成了仆人为了自己方便而禁止主人进家门？主人又如何能心平气和？还有一个例子是高官的座车通过大街，所有的立交行人天桥都要封锁。我曾问过封锁交通的便衣为什么要这样，他教育我说，要服从国家利益，要遵守国家的纪律。国家利益和纪律在此又表现出了和我们正常走路的老百姓的对立。这样的经历多了，我们自然会认为国家的事情不是我们的事情，那是官员的事情，让他们自己操心去好了。在这样的关系中，中国百姓找到了自己的定位。在根基上，他就不认为自己是主人。主人进了家门，难道还需要他聘请的小时工提醒他，要换掉沾满了泥的雨靴再踩地毯么？反过来说，如果我们踩地毯前没有换鞋，而我们又不是不懂事的小孩，恐怕正因为这里不是自己家，连亲戚朋友的家也不是。我们不认为那些换鞋的规矩是自己的规矩，那是官们制订出来管制我们的规矩。其实，不仅在红绿灯前，就是在官场上，这种糟蹋地毯的心态也是很常见的。官员不过是皇上的雇员，能偷懒不妨偷懒。皇上吃香的喝辣的，进进出出还要让小兵小官给他站岗开道，本来心理已经不那么平衡了。凭什么还要对他忠心耿耿？我愿意遵守交通规则，至少不想首先破坏交通规则，是因为我把自己看成一个好人，一个有道德的人，一个己所不欲勿施于人的人。说得自负一点，我耻于把自己降低到那些抢红灯抢座位的人的水平。我觉得自己比他们高得多。而我的这种自我期许的基础又来自哪里呢？我曾经以为自己是一个忧国忧民忧天下的人。国家的事情就是我的事情，民族的事情也是我的事情，人类的事情还是我的事情。我是真把自己当成主人的。秩序是我的秩序，交通是我的交通。我不认为那是警察的事情。我还用警察管么？我那么低档么？<br />
　　我的这种认识和期许最初来自轻信，来自少年时代不加验证地接受的某种关于主人的理论，以为自己真是国家的主人，至少也是主人的接班人。后来我保持了这种自我期许，则是因为西方经济学关于公共服务的道理讲得透彻，从中我看到了理想社会的逻辑。按照这种逻辑，假如我真想成为宪法意义上完整的公民，我就不能妄想只享受公民的权利而不理睬公民的义务。这两种关于主人的理论，一个来自过去，一个来自未来，都不是现实的产物。<br />
　　从这个意义上说，民主制度的真正实行，也是提高公德水平的一个重要的前提条件。因为公德无非是主人翁的道德，不是主人翁，继续当臣民甚至奴才，这道德便没了根基。把上边提到的比喻再发挥一步：如果我雇小时工或者保姆来家打扫卫生，没想到他反客为主，仗着自己膀大腰圆在我家里当上了主子，这时我会作何反应？我被迫出去挣钱养家糊口，被迫给他发工资，这完全是因为没别的办法，不这样就会挨揍。那我还会在乎穿着雨靴踩地毯么？只要这室内卫生的事情还归那位当上了就不肯下台的小时工管，我才不在乎他忙不忙呢。累死他才好，累跑他更好。我怎么会体谅他的辛苦？他是猫，我是老鼠，我体谅猫的心情干什么？以皇帝为最高代表的统治者，就是这样的公仆。公仆如此德行，我的公德应该是什么样子？<br />
　　三大约2000年前，王充在《论衡》中谈论人性问题，说尧和舜当政的时候，百姓没有狂和愚的人。他还讲到一个古代的传说，尧和舜时代的老百姓，可以比屋而封，而桀和纣时代的老百姓，挨着屋子杀掉也不冤。他说，圣主的老百姓和恶主的老百姓如此不同，根源在于化，而不在人性。按照这种说法，刁民乃是政府化出来的，是政府培养出来的，并非天性就刁。当然刁民反过来也能培养贪官。不过要改变这种恶性循环，下手之处当然在政府。这是王充的高见。中国古人讲到尧舜，经常有一些理想化的赞美，但我还是相信王充讲的传说。尧舜时代是什么时代？那是中国的第一个王朝尚未建立的时代，尧和舜都是大家选出来的，每个人都是一个范围不大的熟人社会的成员，彼此几乎都知根知底，而每个人的行为都会影响别人对他的长期看法。人在这样的社会里生活，想不为自己的行为负责是根本不可能的。无论这行为是好是坏，恐怕都不那么容易归功于或归咎于首领或别的什么人。按照我们对原始社会的理解，这些人确实也可以称为贵族，在这样的社会里，比屋而封应该是可能的。到了桀纣时代，人心就不行了，这也是有道理的。桀纣是主子，其他一概都是臣民或奴才。如果主子们像桀纣那样，连一点替天行道的遮掩都不讲，臣民的道德水平自然也应该坏得没遮拦。但我对王充的说法有个疑问。刚才谈论尧舜的逻辑和生活常识都提醒我们，聚族而居或者在小村庄中生活的人，如果不出这个小圈子，其实是坏不到哪里去的，不然就要自作自受。而桀纣时代，血缘社会还没有解体，人们如何能坏到比屋而诛却不制造冤假错案的程度呢？<br />
　　我估计，恐怕那些坏都坏在了公德领域，坏在与公家--王公之类的统治集团--及其掌管的公务的关系方面。而在私德的领域，在家族内部，在朋友邻居和乡亲之间，我们的祖先就未必落后，说不定还能领先世界呢。中华民族长期处于世界先进水平，大概就有这私德的一份功劳。私德好，社会管理成本低，生产者也能安心努力地干活。可是公德不好，先进就保不住，别人不打自己也要乱，早晚要大乱。<br />
　　四如今，我们的血缘社会和地缘社会都解体了，而西方所谓的市民社会又没有建立起来。满大街的人谁也不认识谁，想随地吐痰就吐，想干坏事就干，连丢脸都不用怕，只要能躲过警察就不必承担责任，就可以不遭报应。随着私德领域的缩小和公德领域的扩张，我们的优势没了，劣势却露馅了。中国社会陷人了历史性的危机。<br />
　　这个危险期，至少自明朝起就露出了端倪：因果报应的说法深入人心，关帝庙遍地开花，关云长所代表的无血缘关系的兄弟义气成为我们民族的理想。这些东西都是药，如果我们看见一个人不断吃镇静药，自然可以推断他的神经或精神出了毛病。这种危机似乎还在加重。负责维护公共秩序的政府，本来还有无道的权威支持，可以得到被统治者的认可，统治集团也容易无争议地团结在真命天子周围。现在孔家店被砸了，天道和天命没人信了，本来就很弱的公德领域又少了不受置疑的维护者，中华民族到了最危险的时刻。从这个意义上看，我们现在和过去一样处于大病未愈的时期。如果说有区别的话，无非是试验了几种药，效果都不那么理想。天道的变种和替代品在一百多年的历史试验中先后丧失了权威，人们重建观念世界的努力一次又一次地失败。各种药物的疗效和副作用总叫人不满意。譬如毛泽东思想的猛药，治疗贪官污吏的一时效果不错，但我们在毛泽东时代买东西的时候却整天招人白眼，被那些售货员训来训去。工人农民干活也大规模地偷懒--这都是大家在社会分工中安身立命的领域，表现出来的竟是这副德行。现在，经济发展了，售货员(国营商业还不敢保证)却不敢训斥我们了。这就是社会进步的成就。工人(国有企业的正式工例外)农民和售货员并没有接受更高强度的道德教育，干起活来却认真多了，尽管这认真精神也难免体现在制造和贩卖假货上。我想，这种进步所以能够取得，关键在于现实的报应机制。没有人愿意掏钱买售货员和服务员的训斥，于是老板就要招募懂礼貌的人，就要培训员工笑迎他们的衣食父母。不能约束自己就可能被市场竞争淘汰出局--除非这老板得到了国家特许的垄断地位，当了某个领域的老虎和霸王。这里说的是经济领域。我想，市场竞争在总体的方向上应该有利于实现孔老夫子的己所不欲，勿施于人的理想，因为人们更愿意为包含了这种品德的产品和服务掏钱。政治或者叫公共领域显然不那么乐观。如果像经济领域一样建立某种竞争机制，好服务得到选票，于是升官；坏服务丢掉选票，于是丢官甚至丢掉饭碗，我们在前边为不守公德辩护的理由就要站不住脚了。<br />
　　当然社会生活不仅仅包含政治和经济这两个领域。我的一个在美国生活了多年的朋友告诉我，美国人很怕坏了自己的信誉。他们轻易不敢赖账，轻易不敢违反交通规则。我这位朋友在一个月之内曾经两次驾车超速，被警察叫了下来，登记了他的社会安全号码。不久，他的违规记录就遭到了报应。他接到了来自保险公司的通知，他的汽车保险费用从200元增加到400元。美国人的社会安全号码就好像中国的身份证号码，是惟一的，也是公民得到福利待遇的凭据。但是他如果赖账，如果在公共信用方面有了不良记录，任何跟他做生意的人都会查出这个记录。被他的不良行为害了的银行保险和交通管理之类的部门，自然也愿意登记这个记录。结果，假如你要在一个文明宁静的地方租一间房子，房主一定会要你的社会安全号码，要是你的品德和信用记录不好，就别想以正常价格租到这间房。我的这位朋友认为，这种精密的报应机制，就是美国社会公德水平比较高的根基。这套机制奖励公德，惩罚缺德者。<br />
　　本来，我对发达国家的报应机制的叙述到此就结束了。写完后，我将这篇文章传给一位在英国的朋友看，她说我忽视了一个重要的方面，那就是公正高效的司法体系。征得她的同意，我把她的部分论述翻译过来充实本文：<br />
　　法律和司法系统在维护西方的社会秩序方面起着非常重要的作用。比如，按照法律规定，如果谁超速驾驶或酒后开车，他就要冒吊销驾驶执照的风险。真值得冒这么大的风险吗？如果违反了法律规定，你至少在一年之内无权开车。假如你习惯了每天驾车上班，现在却不得不改乘公共汽车或者出租汽车了，这自然非常不便，而且要为日常交通花更多的钱。更多的麻烦还在后头：即使过一段时间拿回了驾照，你也不再是一个干净驾照的持有者了。那意味着你不是一个老板可以完全信任的人。在英国求职，你必须出示你的干净驾照，以证明你是一个守法的公民。<br />
　　在这里，维护良好社会秩序的核心角色是法律，而不是道德原则。离开了法律对人们行为的规范，道德原则什么也不是，至少是苍白无力的。在西方社会，你可以在超级市场、地铁车站和加油站之类的地方违反规定，但是你不能不面对自己的行为引起的后果。几乎没人愿意去干那种违规的事情，拿自己的前程、职业、家庭生活等等冒险。我们听到过一个故事，有个中国留学生在超级市场偷拿东西，他立刻就被驱逐出境了。在我看来，绝大多数人都觉得违规是不值得的。就是这样。<br />
　　说到司法的公正和高效，我们又回到了政治领域。这真是一个绕不开的下手之处。<br />
　　五走出恶性循环的实际努力开始之后，自然还有不可替代的精神方面的工作。譬如我们要找到一种类似信仰的东西，一种操守的基础。儒家的基础已经被骂倒了，新的信念的基础在哪里呢？<br />
　　我想，这基础其实就在我们的生活之中，无须到天堂和地狱中寻找。譬如造化的报应。前边已经说过了，我们现在正在遭受报应。而且这报应一点也不神秘。我们的祖先一直说天道好还，报应不爽，希望善有善报，恶有恶报。其实造化把这报应的工作交给了人类自己，交给他们设计和建立的制度。如果这个社会倾听这种要求，在尊重个人的基础上建立了权利和义务平衡的制度，建立了恰当的责任追究制度和贡献奖励制度，报应的体制就建立了。如果这个社会不建立这样的制度，那么，这个社会就会遭到报应，它会被其他有着更合乎造化要求的制度的社会所淘汰，或者被它自己所破坏。我们中国的2000年王朝循环就是造化给予帝国制度的报应。<br />
　　报应不仅落到了整个社会和制度的头上，也落到了每个人的头上。我们随地吐痰，我们不排队，我们遇到不道德的国际行径和国内行径都假装看不见。这些严重缺乏公德的行为遭到的报应还不清楚么？我们被人家看不起。我们自己的生活环境和健康环境下降，使得每个人都生活在一个他不喜欢的世界里，这就是报应。为了让自己生活得更有尊严，也更健康，更美好，究竟值得不值得约束一下随地吐痰之类的毛病呢？或者，请一些执法者来管这件事情，而每个中国人都要为他们的工作多掏一笔税金。花钱雇人强迫你，并且用侮辱惩罚的手段迫使你自己不吐痰，然后我们再掏更多的钱行贿，逃避处罚，这不是很愚蠢很可笑么？干吗不约束自己一点，非得获得如此愚蠢可笑的报应呢？造化的报应就是这样的东西，你愚蠢，它就还你一个愚蠢。你可笑，它就还你一个可笑。所有的行为都会计入收支账目，真是天网恢恢，疏而不漏。任何社会和个人都要为自己的失职付出代价，你自己逃避了这种代价，别人就要替你付。人人都能逃避这样的代价，那就要由这个允许逃避的社会和民族的整体付，变本加厉地付。我们的黑头发可以染，黑眼睛和黄皮肤却难以掩饰。造化终究是令人敬畏的，是不容糊弄的。我们自己，我们中国，我们人类，都是这造化的一员，我们今天的行为必定要影响我们的未来和我们的命运。这造化并不仅仅是一个威慑者，它还是一个鼓励者，是一种宽广的、诗意的生活的基础--只要你对它有足够的理解和尊重。中国现在是一个人们正在逃离的社会，这个社会和我们这些社会成员正在为过去和现在的背离造化而遭到报应。我们接受报应，我们不抱怨，这一切都是我们应得的。同时我们要努力，为未来建立新的报应机制，合乎造化的体制，跳出恶性报应的轮回。</div>

							
							
							
							
