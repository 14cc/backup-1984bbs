　　好像雷锋也死了。十多年前，一位不知姓名的父亲抱小孩乘车没人让座，当父亲的便对孩子说了一句：&quot;雷锋叔叔不在了&quot;。这句话的版本之多，流传之广，恐怕要远远超出尼采的那句&quot;上帝死了&quot;。可见时势不仅造英雄，还能造哲学家，至少能造名言。 <br />
