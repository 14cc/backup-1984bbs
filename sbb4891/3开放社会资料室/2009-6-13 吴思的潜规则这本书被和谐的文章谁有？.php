																											<div id="postmessage_121434" class="t_msgfont">探究雷锋<br />
<br />
--------------------------------------------------------------------------------<br />
<br />
　　我想探究雷锋做好事的动机，便重读了雷锋日记。 <br />
　　《雷锋日记摘抄》中记载的第一件好事，是1959年11月14日的&quot;抢救水泥&quot;。那天晚上，雷锋正在屋里看书，突然下雨了。他想起了工地上露天堆放着7000多袋水泥。&quot;这时&quot;，雷锋写道，&quot;我猛然想到了党的教导，要我们爱护国家的财产，又想到了我是一个共青团员。想到这些，一种无穷的力量鼓舞着我，跑到宿舍，发动了20多个小伙子，组织了一个抢救水泥的突击队……&quot; <br />
　　&quot;党&quot;提出了要求，&quot;共青团员&quot;的身份提出了要求，雷锋便热诚地满足了这种要求。1961年农历正月初一，他抵抗住看戏的诱惑，背着粪筐给人民公社拣了300斤粪。300斤不是一个小数。请想象一下，雷锋一个人，孤零零地背着粪筐，在大年初一走在冬日的路上，走了整整一天。如果不是由于他后来光荣牺牲，不是由于他的日记公开发表，他在这一天的行为不会有什么人知道。他不会得到任何外来的东西，除了他自己内心的奖励。那么，在这位年轻人的心里，究竟萦绕着什么样的思想？第二天，1961年正月初二，雷锋把舍不得吃的一斤苹果送给了素不相识的住院病人。我们知道，那是一场夺去了二三千万人性命的大饥荒的最困难的年头。 <br />
　　这样的事情太多了。他在火车上送水扫地，他在半夜三更冒雨送妇女儿童回家。对这一切，雷锋记载下来的内心活动都有一个共同的模式：&quot;我心想，自己是一个共产党员……&quot;，&quot;我立刻想起了毛主席的教导……&quot; <br />
　　真正的问题在于：雷锋为什么会以如此异乎寻常的热情实践党提出的行为规范呢？ <br />
　　在雷锋的日记中，我们可以发现他对共产党的感恩戴德之情，可以发现他对荣誉的珍视和向往，也可以发现他的敏感的同情心。这些因素都可以解释他的热情，但这些都不是主要的。雷锋日记表达出来的最强烈的渴望，是做一个高尚完美的人，是追求生命的意义。他的日记里充满了关于怎样做人的思考。有一次他学习毛主席著作，读《纪念白求恩》，当他意识到自己的矮个子并不妨碍他成为一个高尚的人的时候，竞感动得热泪盈眶。 <br />
　　雷锋是毛泽东门下的贤人，就如同孔子门下的颜回，只是寿命更短。 <br />
　　那么，做一个毛泽东的战士，一个理想中的共产党员，对雷锋和他的同时代人究竟意味着什么？ <br />
　　有一种宣扬了数十年的信念：如果你投身到伟大的共产主义事业之中，成为一名完美的共产主义战士，你就会体验到最广大最深刻的幸福，你的生命就充满了意义，你就能克服个体生命的渺小和有限，进入不朽的无限境界，即使你死了，也死得重于泰山。 <br />
　　在我的眼界里，这就是雷锋所追求的最深层的东西。颇有点像一种古老的寻宝的神话――如此这般地克服了障碍，从此就幸福地生活着。 <br />
　　雷锋在记日记的时候，几乎还是一个孩子。他的日记摘抄起于18岁，止于22岁。他记载下来的是一种青春期自我塑造人格的努力，一种如何度过人生，做一个什么样的人的最初的尝试。雷锋没有机会继续往下走，没有机会三十而立，四十不惑，五十知天命。而时光对人格的作用就像对醇酒的作用一样重要，即使圣贤也不能例外。 <br />
　　如果雷锋有机会继续走下去，他会变成什么样子？没人能够断言。不过我相信，他会遇到许多困惑，比如他决心热爱的人民在他热心信奉的人民公社的土地上偷懒，他热爱的党组织竟会出现贪官污吏等等。同时我也相信他的眼界会像我们一样开阔许多。他会发现，关于人生根本问题的答案有很多种，从佛陀到耶稣，从孔子到毛泽东，人类的圣贤一直在寻找着自己的答案，这种寻找至今仍在继续。 <br />
　　应当说，如今比雷锋生活的时代有了更好的寻找条件。35年过去，人类又积累了巨量的经验教训和各种知识。这些知识可以帮助我们更透彻地了解人类和万物之间的关系，帮助我们找到自己的根本和归宿，帮助我们进人更成熟更高明的境界。问题在于，我们有没有雷锋那样的真诚和热情。<br />
