													<h2>日光之下无新事：电影《我在伊朗长大》</h2>
						
						
																											<div id="postmessage_5" class="t_msgfont">作者：王怡<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 快翻年了，又有一部坦克上街的电影。三联书店出过，法国的伊朗裔女作家玛嘉・莎塔琵的自传体漫画《我在伊朗长大》，包括《面纱》、《安息日》、《捉迷藏》和《回家》。这部老掉牙的黑白动画片，竟在去年戛纳电影节上得评委会大奖，就因为黑白分明，而世界混沌。一如伊朗妇女的服饰，也一如1980年代的伊朗世界。<br />
<br />
　　在哪里长大，都可能爱上这部电影。就像齐秦和齐豫唱：“你在天空飞翔，我在地面流浪，看似两个地方，其实都是一样”。得到天空的，失去大地；困守城池的，苦苦仰望。今天下午，当我蹲在地上，找到瑞典乐队Club 8的新专辑《停不了做梦的男孩》；就想起电影里的少女玛嘉，在德黑兰的街头黑市上，买朋克乐队Iron Maiden的磁带，被警察抓。区别只是Club 8的音乐那么平缓，比黑白动画还要陈旧。而Iron Maiden的吼叫，却如帝国大厦顶端那只绝望的金刚。曾有一回我去帝国大厦，在外面仰望了一阵，好像金刚会出现似的。心想，同样是街头的梦魇，金刚和坦克的区别是什么呢。<br />
<br />
　　对玛嘉来说，伊朗和世界的区别又是什么。全盘西化过，共产主义过，革命过，镇压过，共和国过。最后霍梅尼上台，“不要西方，不要东方，只要伊斯兰”。但一个政教合一的体制，一种布满道德警察的宗教化，又怎么可能带来真正的信仰复兴。宗教，不过成为在全面失忆的身份危机中，一个刻舟求剑的记号，一个民族的LOGO。以最反潮流的方式应对潮流，仿佛一根稻草，不是为拯救，只为了抢注商标。就如以色列人被掳归回之后、直到公元一世纪这四百年间，在外邦人的殖民下，他们既没了君王，也没了先知。于是持守律法的宗派就日益兴盛起来，严苛的戒律，成为对生命意义的一种临摹。这和今天伊斯兰世界的某种坚持颇为相似。和今天某些鼓吹儒教国教化、或祭孔大典的人士也异曲同工。<br />
<br />
　　当年国王被赶跑，示威被镇压；之后什叶派的戒律传统，在革命之后，成为20世纪末期一张波斯帝国的名片。玛嘉的爷爷是一位有皇室血统的王子，叔叔是流亡苏联的革命家。8岁时，她就手持标语，在自家客厅游行，一二三四地去睡觉。<br />
<br />
　　这部电影动人，因为它以一个小女孩的成长，见证了伊朗30年的历史。它一得奖，伊朗电影机构就宣称，这是一部“没有真实反映伊斯兰革命伟大成就”的电影。然而童言无忌，在少女玛嘉看来，所谓伊斯兰革命就是舞不能照跳、马不能照跑；就是8岁到80岁的女人都穿得一模一样，耐力球鞋成为西方腐朽文化的代表；就是半夜开舞会，被警察逼得跳楼身亡；就是老师带头撕掉课本上讲到国王的页码，在课堂上撒谎说现在没有政治犯。但玛嘉站起来说，我叔叔被绞死了；有国王时，有3000政治犯，没有国王后，变成了30万，“您怎么能用谎言来欺骗我们呢”？<br />
<br />
　　父母设法把玛嘉送去了维也纳读书。影片的第二个动人，是以往那个在海报、电影、磁带和日用品中的西方世界，结果也在玛嘉的流浪中动摇了。她的梦想只是叶公好龙，那个世界她融入不了，也割舍不下。她的身份在伊朗和欧洲之间，像一只股票随行就市。原来虚空的虚空，一切都是虚空。她的奥地利同学说，“生活本身就毫无意义”。难道和伊朗相反的，只有一种可能？来自伊斯兰世界的玛嘉，平安夜真的与她无关；她哭着打电话给母亲，说我要回来，但你什么问题都不能问。<br />
<br />
　　母亲说，回来吧，我什么都不问。电影的黑白风格，此时释放了它强大的感染力。一个伊朗人，怎么可能重新成为一个人，却仍然是伊朗人？在哪里成长不都这样吗。80年前，共产党人恽代英说，“多一个基督徒，就少一个中国人”。每个基督徒直到今天，都在龙的传人和上帝的子民之间，面对破碎，和一个被更新的民族身份。一个北京的穆斯林朋友则说，他在书店，站着读完了《我在伊朗长大》的漫画，边读边流泪。而在石门坎，连文字都是传教士为他们创设的。彝族人说普通话，看新闻联播，星期天去教堂。《色戒》如何撩人，而祭孔又与他们何干？<br />
<br />
　　第三个动人的，是法国与伊朗的对照。尽管玛嘉第二次从祖国落荒而逃，就一直住在法国。她说喜欢法国可以随便抽烟，工人可以随便罢工。尽管常常没有车坐，没有电用，她也不想回去了。那个胸口散发茉莉花香的奶奶已经去世。这位曾经的准王妃，是一位真正的穆斯林；一个不需要枪口和道德警察的保守主义者。玛嘉第二次在街头因为没戴面纱而被警察盘讯，她胡乱指责旁边一位男子，说自己受到性骚扰。她对奶奶说，我没得选择。慈祥的奶奶却说，我只能看你是娼妓。你有选择，在任何情况下都有。<br />
<br />
　　玛嘉在伊朗失去天空，在欧洲失去大地，在伊朗失去了自由，在欧洲失去了道德感。电影动人，不但在于她充满了幽默和小女孩的自嘲感，也在于这一份漂泊的张力，她没把喜欢呆着的法国当作伊朗的未来。因为呆着只是呆着而已。法国的穆斯林已达人口的六分之一。族群认同的混乱，日益成了它最严重的社会问题。我曾在那里见过许多官员，一旦问起禁止在公立学校表露信仰的《头巾法》，他们态度都很生硬，甚至反感。说这是保护政教分离，不是侵害信仰自由。一位国民议会的顾问，还差点跟我吵起来。但法国政府唯一的宗教事务办公室，也告诉我，在2006年的民意调查中，承认自己有种族歧视的法国人，已占到三分之一。<br />
<br />
　　我没见到玛嘉・莎塔琵对《头巾法》的评论。可这是什么世界啊，在那一头上学，你不可以不戴头巾。在这一头上学，你不可以戴头巾。地上的城，一座连着一座，你在这里成长，我在那里成长。只是日光之下，并无新事。我是谁？从哪里来，到哪里去。除非人有确信，不然过去的世代，无人记念；将来的世代，后来的人也不记念。<br />
<br />
　　就如齐豫《九月的高跟鞋》：<br />
<br />
　　“脱下疲倦的高跟鞋，赤足踏上地球花园的小台阶，我的梦想不在巴黎、东京或纽约，我和我的孤独，约在微凉的、微凉的九月”。<br />
<br />
　　寂寞是最流行的盾牌。但地上有不动产，天上有永存的房屋。人若看见自己是客旅，是寄居的，人还有回去的机会。玛嘉更美的家乡，的确不在伊朗，也不在巴黎。<br />
<br />
　　我们又如何。</div>

							
							
							
							
