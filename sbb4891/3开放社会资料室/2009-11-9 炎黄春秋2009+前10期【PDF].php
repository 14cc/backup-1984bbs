　　在新年将至的时候，许多作者和读者来电来信，询问杂志社的情况和今年的编辑方针，借此机会，我们向大家汇报一下我们的情况和想法。 <br />
　　今年是《炎黄春秋》创刊18周年。17年来，我们始终坚守一条原则：实事求是。誉人不增其美，毁人不益其恶。我们有很多缺点和不足，但自信守住了这条底线。 <br />
　　我们走过了17年，虽然经历了许多风雨，但读者在增加，作者在增加，页码也在增加，社会影响越来越大。这要感谢读者对我们所坚守的原则的支持，也要感谢各方面的善意、理解和宽容。在新的一年里，我们的编辑方针将一如既往，实事求是地对待历史，努力推动民主和法制的进步。 <br />
　　现在，社会各界都在谈论改革开放30周年的经验。新的一年，我们还要探讨建国60周年的经验教训。对经验教训的探讨深度，不仅表现了探讨者的眼光、意图和利益，还会影响中国未来的选择和走向。 <br />
　　在我们看来，改革开放30年的成功经验可以概括为一句话：扩大公民权利。套用八十年代常说的四个字，就是“放权让利”。大包干来自放权让利，解决了中国的吃饭问题。民营企业崛起来自放权让利，在不平等竞争的条件下超越国营企业，对国民生产总值的贡献将近70％。 <br />
　　这条成功经验背后的规律是：有一分权利，有一分自由，就有一分发展。所谓自由，就是在自己应享权利的范围内自主选择并承担其后果，多付多得，少付少得。不受外人限制，也不被他人剥夺。如此，才能在不侵犯他人利益的条件下，充分调动每个人生产和创造的积极性。 <br />
　　改革开放的伟大成就证明：以邓小平为核心的领导集体，汲取了从大跃进到文革的惨痛教训，改弦更张，尊重公民权利，放权让利。现在的领导集体强调以人为本、科学发展，建立和谐社会，这个“人”应理解为中国公民，理解为公民所享有的各项权利：这种主张拥有60年经验教训的深厚依据，坚持下去，必将大有成就。 <br />
　　按照“有一分权利，有一分自由，就有一分发展”的规律，我们认为，中国今后的前进方向，仍然是扩展公民权利。以扩展公民权利为核心的发展，便是科学的发展。如此不仅可以解决经济问题，还可以解决政治问题甚至道德问题。 <br />
　　去年爆发的金融危机有可能演变为全面经济危机。目前提出的基本对策是扩大内需。扩大内需，就应该提高劳动者的收入，调整工人、农民、企业和政府之间的利益关系。在这个领域，工会、农会和商会都可以发挥重要作用，有助于保护各自成员的权益，实现社会公正。在税收领域，是更多地藏富于民，还是更多地替民做主，也应该经过公众讨论，更加尊重民众的选择。在财政领域，公共工程、社会保障体系和其他投入项目孰轻孰重，通过广泛而公开的讨论，把选择权更多地交给民众，可以避免重大失误。 <br />
　　如果把市场看做经济领域的激励机制，那么，民主就是政治领域的激励机制――激励政府官员尽心尽力地为人民服务，而不是为自己和为上级服务。这两个领域的改革是无法相互替代的。我们的经济体制改革取得了伟大的成就，如果把成功的经验推广到政治体制改革领域，必将取得同样伟大的成就。 <br />
　　从长时段看来，中国历史上不乏经济繁荣和太平盛世，但治乱交替却成了我们民族的宿命，形成了所谓的周期率。我们现在超越自身了吗?1945年，毛泽东主席说：“我们已找到了新路，我们能跳出这个周期率。这条新路，就是民主。只有让人民来监督政府，政府才不敢松懈：只有人人起来负责，才不会人亡政息。”毛泽东主席对历史经验教训的总结深度堪为今人楷模。现在，不故步自封于经济建设的成就，勇敢地踏上新路，这是比战胜经济危机更加重大的历史使命。 <br />
　　政治体制改革的核心就是扩展公民权利、保障公民权利，即扩大民主、完善宪政。这方面的进步，也是我们重建价值观的基础。这方面的进展，将健康而持久地推动中华民族在道德及整体精神文明领域的进步。 <br />
　　总之，我们的编辑方针，我们的坚持和追求，就源自我们认识到的历史逻辑。<br />
