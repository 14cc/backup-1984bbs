　　黄顺兴刚当常委时，曾提出过一些建议，包括在第二次常委会上提出的允许记者进入大会会场采访。他说：“人大号称最高权力机关，类似现代国家的国会，国会怎么可以不许记者进会采访？国会讨论的情况怎么可以不马上传播出去与大众见面？外面的意见怎么可以不迅速返回来？如果这些都没有，怎么能具代表性？要建立这样一个循环，媒体上的记者是少不了的。世界上无论那个国家，包括独裁的蒋介石政府都有，为什么人民民主的共和国反而没有？”当时赵紫阳在位，万里转达了黄顺兴此建议。<br />
<br />
　　开放记者后，一次，一个记者问黄顺兴可否谈一谈。黄问是不是采访，能不能报导？记者说不能，采访发稿必须大会统一。黄说：如果这样，我没有必要和你谈，咱们只做朋友好不好。记者说：我和你谈的，虽然不能公开发表，但我可以写《内参》。黄问《内参》是什么，记者说那可重要了，头头们看的，别人想看都看不到。黄说：“我是人民，人民是最高的。为人民服务，重要的是外参，而不是内参。对不起，我不接受这种采访。”<br />
<br />
　　1989年，在黄顺兴建议下又通过了《议事规则》。黄提出：不管小组讨论怎么样，大会是全体代表沟通的最后一个机会，这个权利不可被剥夺。这个《议事规则》也通过了。<br />
<br />
　　1992年3月，七届全国人大五次会议召开，其中一个议程是审议国务院关于提请审议兴建长江三峡工程的议案。<br />
<br />
　　黄顺兴找到秘书处，问：你们准备的成套的力促上马的“正面材料”看到了，有没有另一方面的意见？我两面都想了解。秘书处说：台湾团只有一份，只能借用，不能给你。黄说：你们做正面推动性宣传，花那么大的气力，每位代表光材料就几公斤；而不同意见，只薄薄的几小册，怎么就不能发给每人一套？三峡工程的决议案要在大会上表决，不了解全面情况，怎么发表意见？也许有人愿意放弃权利，但我不，我要我的一份，你想办法给我找来。<br />
<br />
　　开会之前，黄顺兴将10本负面材料复印，并发给希望知道全面情况的代表。但大会封锁，不同地区的代表之间不准直接交往，材料只能送到所在团的秘书处。但一到那里，就如泥牛入海，对方根本不可能收到。在会上，秘书组单独给了黄一份反对方意见，说：“黄老，这是特别给你的。”黄说：“我不要特别，了解全面情况是每个代表的基本权利。”<br />
<br />
　　人大《议事规则》第54条规定：想在大会上发言的代表，须事前向大会秘书处登记，大会主席即应安排发言。临时动念者，经大会主席许可始得发言。代表第一次发言时间10分钟，第二次5分钟。黄顺兴在3月31日向大会秘书处登记发言。<br />
<br />
　　1992年4月3日，第一案、第二案已经通过，下面就是三峡工程的表决了，黄顺兴开始喷药――他有病在身，胸腔里装着心脏起搏器，情绪激动是很危险的，提前三分钟喷，可管半小时。<br />
<br />
　　表决开始了，黄顺兴登记的发言果然没被安排。黄在座位上举起手，要求即席发言，但主席不予理睬。这时，突然听到台湾记者喊：“没有声音了！没有声音了！”原来，整个会议大厅的音响系统，一刹那间通通切断，只剩下主席面前的麦克风还能工作。<br />
<br />
