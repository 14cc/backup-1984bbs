<br />
<a href="https://0-focus-opensocial.googleusercontent.com/gadgets/proxy?container=focus&amp;gadget=a&amp;rewriteMime=image/*&amp;refresh=31536000&amp;url=http://docs.google.com/File?id%3Ddg5pc3d3_1849r95kk7g8_b" target="_blank">https://0-focus-opensocial.googl ... c3d3_1849r95kk7g8_b</a><br />
<br />
在2010年4月首次出版的《一叶一菩提――我在敦煌十五年》是一本回忆录，详细讲述了位于中国西北的敦煌佛教石窟在文革期间，如何逃过劫难，从红卫兵手下被保存下来的故事。本书在中国已经被正式禁止出版和发行。下面是《环球时报英文版》的报导：<br />
<br />
“对敦煌的掠夺史中，最有警示意义的例子发生在19世纪，道士王圆禄在发现了藏在洞窟中的数千本古代卷轴后，把它们卖给了外国的冒险家，包括英国探险家斯坦因(Aurel Stein)，法国语言学家，汉学家保罗・佩里奥(Paul Pelliot)和美国艺术史学家华尔纳(Langdon Warner)。这些无价的卷轴是玄奘和尚翻译的佛经。玄奘在公元629年通过丝绸之路到达了印度，并把佛教雕像带回了中国。”<br />
<br />
在敦煌呆过15年的权威考古学者，现年73岁的萧默说，尽管该机构的负责人常书鸿受到了多年的迫害，但是敦煌洞窟却毫发无损。<br />
<br />
萧默在今年4月出版了《一叶一菩提――我在敦煌十五年》，这本书主要讲述敦煌在文革中的遭遇。<br />
<br />
“我想反映真相，并展示在悲惨的历史时期中的展现出人性光辉的行为。不幸的是，我接到通知说这本书被禁止发行、宣传或者重印，”他昨天对《环球时报》说。“我不明白为什么书会被禁，我很伤心”。<br />
<br />
在北京的王府井书店和新华书店的经理都说，他们没有收到任何关于禁止萧默的书的官方通知。当被问到为什么这些书已经下架两周时，两位经理解释，因为这些书“断档了”。<br />
<br />
据《南华早报》的报导说，本书的作者萧默在为这本书寻找香港的出版商――这本书目前只有中文版。从1963年到1978年，萧默一直在甘肃的敦煌研究院工作。<br />
<br />
“这个月初，萧默高兴地发现他的这本在4月出版的书已经在网上书店中几乎卖光，而且出版商要求再版。”<br />
<br />
萧默的出版商通过电子邮件告诉他，在8月4日和8日，中央出版部门官员两次审问出版社的负责人，为什么要出版这本书以及这本书的整个出版过程。一些编辑被迫写了检查。过了些天，他们就收到了中央出版部门的不准重印该书的禁令。<br />
<br />
一位出版商的编辑确认了禁令，并说，在7月，这本书在卖光了首印的5,000本后就收到了禁令。<br />
<br />
“我收到的读者反馈都说这本书的情节引人入胜，”萧默说。“我只把焦点集中到文革期间正面的人性方面，一些基层领导尽他们最大的努力用自己的权力来保护别人。”“我努力避免提及文革期间的暴力，并尽量使用幽默的口吻。这是一个讲述文革的新角度，也是一个突破。我想不通这本书为什么会被禁掉。”<br />
<br />
