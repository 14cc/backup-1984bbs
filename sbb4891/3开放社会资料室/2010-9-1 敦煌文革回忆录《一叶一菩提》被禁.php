													<h2>求书《一叶一菩提》萧默著</h2>
						
						
																											<div id="postmessage_815200" class="t_msgfont">声援先进文化<br />
――就《一叶一菩提》被禁批判文化专制主义<br />
杜&nbsp;&nbsp;光&nbsp; &nbsp; <br />
<br />
听到萧默先生的近著被查禁的消息，我始而有些惊讶，继而感到愤怒，觉得自己有责任发表意见，谴责文化专制主义的新罪行。<br />
<br />
萧默先生的《一叶一菩提》真实记录了他在敦煌15年的生活历程，其中包括文化大革命的10年。他以他所特有的平和、恬淡、甚至不乏幽默的笔调，勾画出人性的善良美好和优秀传统文化的传承，不但留下了忠实的历史资料，而且对教育被拜金主义扭曲了人性的当代青年，也有着十分积极的意义。<br />
<br />
在充满了虚伪说教、陈词滥调和低俗拜金的出版物之林，突然出现了这样一部洋溢着真善美的好书，自然引起了舆论界的注意，一时好评如潮。出版后两个月里，全国就有二十四五家报刊竞相报道、评介、推荐，其中有十余家以整版篇幅加以宣传，有的多至三版，《新京报》C版的“书评周刊”甚至还登出了萧默的整版头像，网上推介就更多了。在舆论界热烈推介下，这部书登上了好几家书店的畅销榜，初版5000册很快便销售一空。一部兼有史学价值和文学意义的新书能得到这样热烈的介绍和欢迎，大概是1989年之后很少有过的了。<br />
<br />
就在作者和出版社、书店都在期待着加印的时候，文化专制主义伸出了它的魔爪。出版此书的出版社社长被召到中宣部，受到详细查问，怎样出版的？为什么要出版等等。接着，新闻出版总署不通过任何法律程序，也不通知作者，就悍然下令：“停止发行，不宣传，不加印。”一部很有价值、很有意义的好书，就这样被扼杀了。<br />
<br />
文化专制主义在２００７年年查禁了八本书，引起了作者章诒和等的强烈抗议，海内外舆论同声谴责，迫使下达禁令的新闻出版总署否认自己的禁令，出尽了洋相丢尽了脸。人们本以为他们会学得聪明一点乖一点了，不料现在又故伎重演，再一次地用不得人心的手法，查禁得人心的好书。<br />
<br />
文化是一个政党、团体或个人观照自己形象的镜子，但不是任何政党、团体、或个人可以代表得了的。江泽民在2000年初提出“三个代表”，并且载入中共十六大通过的党章，其中就有“代表着先进文化的前进方向”。这里姑且不论这个判断在理论和实践上仍可讨论之处，就算能代表或想代表，那么，作为中共分工过问文化领域的中宣部，总应该创造条件，支持、鼓励先进文化，指明它的前进方向吧！什么是先进文化呢？应该是能够促进社会发展进步的文化，应该是体现人类对真善美的追求的文化。文化是否具有先进性，就看它是否真善美，真善美既是先进文化的内涵，也是先进文化的前进方向。用这个标准来衡量，中宣部在新世纪的所作所为，恰恰不是支持、鼓励先进文化，指出先进文化的前进方向，反而是查禁、扼杀先进文化，与先进文化的前进方向背道而驰。这几年来，他们查禁了多少优秀的著作，恐怕他们自己也说不清楚了。他们抹杀真善美，维护假恶丑，成为真善美的刽子手，假恶丑的保护神。他们甚至直接炮制假恶丑，流毒全社会，例如他们去年编写出版的《六个“为什么”》，就是假恶丑的典型――假马克思主义，丑陋的陈词滥调，恶劣的社会影响。<br />
<br />
中宣部是中共中央的一个职能部门，它的工作，顾名思义，应该是宣传共产党的纲领、理论和政策方针，给文化的发展提供自由宽松的环境，以对文化的宽容宽厚，为共产党树立良好的文化形象。它没有必要也不应该去充当文化专制主义的黑手。可是，那些自以为掌握着文化意识形态统治权的大人们却一意孤行，在文化领域倒行逆施，不遗余力地对真善美的先进文化大肆堵截禁绝，结果许多真正具有学术价值和社会意义的作品，不得不送到香港、台湾或海外去出版，或者自费印刷，但往往又被列为“非法出版物”而横遭扣留、查抄，这就极大限制了它们的应有作用的发挥。即使有些经过出版社严格审查、反复修改才得以合法出版的书籍，一旦被认为其中有哪怕一点点不符合文化专制主义口味的内容，就全盘否定，立刻下达禁令，斩尽杀绝。这些仗恃暴力的可耻行径，不但扼杀了先进文化的发展生机，而且给已经被贪污腐化抹得空前丑陋的政党形象，又一次一次地涂上不堪入目的污垢。中宣部这种逆历史潮流而动的文化专制主义，迟早会被扫进历史的垃圾堆，而那些老是挥舞着专制主义大棒的权贵官僚，则将在人们的唾骂怒潮中声誉扫地，身败名裂。<br />
<br />
其实，专制主义对于先进文化的打压，有时反而起到了扩大宣传的作用。章诒和的《往事并不如烟》已有先例，萧默的《一叶一菩提》也必将会踵随其后，以更大的规模，在社会上广泛流传，发挥它的人性教化的作用。<br />
<br />
萧默先生表示，对于《一叶一菩提》的被查禁，他将据理据法，抗争到底。我全力支持他的抗争，并且希望能得到全社会的同情和支持。真善美终会战胜假丑恶，这是无可怀疑的。<br />
<br />
2010年8月19日</div>

							
							
							
							
