&nbsp; &nbsp; 那天夜里，宫润柏坐在女孩冰凉的身体旁看见整个世界的色彩如同干燥的沙粒被风吹去，一团团没有颜色的火焰在大地上熊熊燃烧，他感觉自己已经被烤成了焦炭，却无处可逃，他想起在那个黑暗的通道中，他父亲泪飞如雨，对他说：“没有尽头。”<br />
&nbsp; &nbsp; 天刚刚透亮的时候，他抄起平时切肉串用的那把刀，一刀一刀将女孩分开。<br />
<br />
&nbsp; &nbsp; 2006年的一天，男孩王俊霖躺在宫润柏的床上努力控制着不让自己的抖的太厉害，就在他和宫润柏的身旁，还躺着四具内脏被掏空的男孩的干尸，王俊霖想自己应该装作对这一切都无所谓的样子，他问宫润柏：“老哥，干嘛不找个女孩？”他装出有点色的样子说：“和女孩做多舒服？”<br />
&nbsp; &nbsp; 刚刚还凶暴无比的宫润柏突然哭得像个孩子，边哭边对王俊霖说：“女人没一个好东西，我原来结过婚的，我还有个孩子，我们很爱他们，但她却嫌我穷，带着孩子跟一个有钱人跑了。”<br />
&nbsp; &nbsp; 王俊霖说：“那我以后也不找女人了，老哥，以后我就跟你。”<br />
&nbsp; &nbsp; 宫润柏有些疑惑地看着他，王俊霖指着几具干尸说：“老哥，我都听你，你可别把我杀了弄成他们那样。”<br />
&nbsp; &nbsp; 宫润柏有些感动地抱住他说：“只要你对我好，我就绝不伤害你。”<br />
&nbsp; &nbsp; 那一夜宫润柏和王俊霖讲起自己曾经是一个优秀的大学生，他在学校里认识了他的前妻，是他们学校的校花，他们当年爱的死去活来，除了他，她谁也不嫁，毕业以后他们结婚了，有第一孩子的时候，他高兴坏了，他把家里的家务活全包了，他给她端茶送饭，帮她洗脚按摩。宫润柏说：“为什么了他们我什么都愿意做。”突然他开始哽噎，又开始哭，他说：“可是她把我抛弃了，她走的那天我苦苦哀求她，可是她只是那样漠然地看着，我问她我犯了什么错，只要告诉我我就改，我问她我该怎么做，她只是那样漠然地看着我。”<br />
&nbsp; &nbsp; 第二天，王俊霖对宫润柏说：“你这里太冷了，我爸妈这两天去我爷爷家了，干脆去我们家算了，看看顺便拿点钱。”<br />
宫润柏犹豫了一下说：“好吧。”<br />
&nbsp; &nbsp; 王俊霖打开家门时，宫润柏看到那个小小的客厅里坐着五六个人，宫润柏并没有什么反应，王俊霖的妈妈看到自己的儿子失而复得，扑过来抱住了就哭。宫润柏对王俊霖说：“我走了。”王俊霖只是抱住妈妈失声痛哭。<br />
&nbsp; &nbsp;&nbsp;&nbsp;在回去的路，宫润柏嘴里咕噜着“小骗子”，他觉得自己好像应该做点什么，却又懒得去想，他走进麒麟网吧坐下开始玩游戏。<br />
&nbsp; &nbsp;&nbsp;&nbsp;两天以后，警察来到了麒麟网吧，宫润柏问警察：“能快点审判吗？真希望能早点结束。”<br />
