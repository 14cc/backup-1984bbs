													<h2>谈禁书电子化工程</h2>
						
						
																											<div id="postmessage_720969" class="t_msgfont">原帖链接：<a href="http://hexiecollege.blogspot.com/2010/07/blog-post.html" target="_blank">http://hexiecollege.blogspot.com/2010/07/blog-post.html</a><br />
作者：北国<br />
<br />
从建站到现在，本站的管理层一直努力将注意力集中在禁书这一领域，我们开办的每一个栏目(如电子书下载、禁书资讯、翻译等)都与此主题紧密相关。<br />
<br />
因为我们知道，禁书制度作为中共出版审查的产物，已经深刻的影响了中国社会的每个阶层、每个人、每一寸土地，它从思想上阻止了大众的思考和创造，剥夺了大众平等获取知识信息的权利，使得信息成为权力阶层的独享品。这样一来，一个等级森严的阶级社会就被中共炮制出来了，现在这个社会是建立在信息不对称的基础之上的。<br />
<br />
从禁书的种类上来说，禁书的范围并不限于政治类书籍，各个学科的书籍都可以被禁，其原因则千奇百怪。其中最常见的被禁的原因是，书中的真实的记载，不同于官方的虚假版本，所以被禁。如赵紫阳的《改革历程》，高耀洁关于中国艾滋疫情的一些著述，以及韩国历史的一些书籍。<br />
<br />
我们认为，只有充分的掌握信息，才能够做出正确的判断。而当代的社会上，很多青年对于这个国家的历史与现实一无所知，却自称有理想有抱负，这样的人他们的世界观能够靠谱吗？<br />
<br />
现在互联网上的一些热心的网民，将一些禁书陆陆续续的扫描上网，这极大的丰富了网民们的精神世界。虽然GFW的存在，给信息的传播制造了一些麻烦，但是互联网上的信息流动看起来是不可阻挡的，透过GFW放射到墙内的信息像涓涓细流一样，滋润着中国大陆这片信息的沙漠。<br />
<br />
然而，目前互联网上的禁书电子化工程，还是处于无组织，无计划的阶段。还没有这么一个专门的组织来整体促进这项有意义的事业。我们在本站的小组里讨论这件事情时，有的网友提到了版权的问题，我们认为，在目前大陆实行禁书政策的情况下，实体书根本无法在大陆销售，可以考虑通过Google的即将运营的在线电子书商店，来专门的销售禁书的电子版。<br />
<br />
从数量和规模上来说，Google Book是互联网上空前绝后的图书电子化计划，本站在Twitter上介绍过Google Book制作的一个禁书专题，但是那个专题仅仅是针对美国读者而言的，其对于禁书定义也和中国不同。<br />
<br />
事实上，Google在进行大规模的图书电子化的同时，顺便也将美国各大图书馆中馆藏的所有中国禁书都扫描成了电子版。只是由于版权政策的限制，Google Book里的大部分禁书还无法和读者见面。尽管如此，Google Book里还是有数量可观的禁书可供阅读，这些有版权的禁书通常被Google设置为“有限预览”，即隐藏了这本书的若干页面。<br />
<br />
为了方便网民查阅，我们在Google Book建了一个禁书书单，收录了Google Book上的可读的禁书，希望对于大家有所裨益。部分书籍需要用美国IP才可阅读，请自行翻墙查看。书单刚刚建立，收录禁书尚少，今后会不断补充，如果你们发现了好书，欢迎到我们的小组推荐，我们会及时加入到这个书单之内。<br />
书单：<a href="http://books.google.com/books?lr=&amp;uid=12068933242558532450&amp;as_coll=1008" target="_blank">http://books.google.com/books?lr ... 50&amp;as_coll=1008</a><br />
<br />
无论你是谁，无论你在墙内还是墙外，让我们一起为禁书电子化及传播而努力吧。<br />
