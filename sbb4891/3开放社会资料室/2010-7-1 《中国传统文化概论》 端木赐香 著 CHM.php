《中国传统文化概论》 <br />
<br />
第一讲　漫谈中国传统文化 <br />
第二讲　中国文化中国人 <br />
第三讲　中国文化中国男人 <br />
第四讲　中国女性文化 <br />
第五讲　中国贪官文化 <br />
第六讲　中国清官文化 <br />
第七讲　农业神话与农民文化<br />
第八讲　中国传统士大夫文化<br />
第九讲　中国传统婚姻文化（上）<br />
第十讲　中国传统婚姻文化（下）<br />
<br />
序：<br />
<br />
　　作为一位大学历史老师，在面对中国历史的时候，我有诸多不快。这种不快，不是一两句话就能概括的，简单说吧：我不能容忍，不能容忍中国历史本身的发展逻辑！中国历史给我的感觉就是千年轮回，如果说拉丁美洲是百年孤独的话，那中国何尝不是千年孤独呢？这方面，黑格尔说得很不客气，他说：“中国的历史从本质上看是没有历史的；它只是君主覆灭的一再重复而已。任何进步都不可能从中产生。”在当代法国政界与学界都很出名的阿兰・佩雷菲特对黑格尔这段话表态如下：“要批驳黑格尔关于中国处于停滞不变状态的观点很容易……然而，黑格尔是对的。”很显然，他不愿意反驳黑格尔，而且，他利用自己的探索来给黑格尔的话作注脚，他说：“1960年8、9月间，我从香港出发，对中国进行了第一次探索。我马上就吃惊地看到这个社会同马戛尔尼的伙伴们描写的社会十分相似。简直可以说每个中国人的基因里都带有乾隆帝国时的全部遗传信息。”<br />
<br />
　　看到这些“局外人”对中国历史的评论，我更不快，但是我无力反驳，也不想反驳。更何况，我们阵营内部的“自己人”也有类似的表态，马克思听说了太平天国的故事后，隔着千山万水表态如下：“显然，太平军就是中国人的幻想所描绘的那个魔鬼的化身。但是，只有在中国才能有这类魔鬼。这类魔鬼是停滞的社会生活的产物。”<br />
<br />
　　让人痛心的是，“局外人”的评论，常被我们当作“别有用心”和“不安好心”；“自己人”的评论，深刻的、带刺的，我们就假装听不见、看不见。作为一个历史学人，我认为我的任务不是反驳。也许我的反驳会很精彩，但是精彩的反驳无助于社会的前进与苦难的消逝，正像漂亮的烟花绽放后无助于空气的清新与自然界的美丽一样。当然，不反驳，并不意味着沉默。轮回的历史，意味着苦难的重复，如此情形之下，沉默只能是一种不道德的选择！更何况，沉默虽然可以成为一个学人自保的铠甲，但是历史经验告诉我们，这类铠甲只能起暂时的保护作用！丧钟既然已经敲响，那么谁会是例外？<br />
<br />
　　没有人是例外，我无权置身事外！这种强烈的“局内人”的身心意识，导致我在做研究时，更多地具有自揭伤疤的痛楚。更严重的是，长期的研究促使我认定，导致中国历史轮回的原始推动力就是文化――中国传统文化！文化乃制度之母，中国古代那种超稳定的封建制度系统之能坚持两千多年，以及这种系统之下社会生活各个层面的消极因素在清帝被推翻后的继续存留，使得我们不得不对自己的传统文化进行反省、重估与清点。更关键的是，时至今日，尽管大家对现代化的概念有多种界定，对现代化的模式也有多种解释，但是无庸讳言，几乎全球所有的发展中国家都将现代化作为国家发展的口号与目标。一句话，中国离现代化尚有多远？中国传统文化中的哪些因素与现代化精神背道而驰？或者说，是哪些因素，妨碍了我们走向现代化的脚步？<br />
<br />
　　目前看来，中国现代化进程正在经历着“社会整合”阶段。在这一阶段，政治、经济、文化、价值观、生活方式等因素，尚没有融合为一个合谐的整体。具体来讲，有些因素，比如经济，可能现代化色彩更浓一些，因为中国经济更多地转向了企业改制、转向了市场化；而有些因素，比如政治与法律，则更多地具有传统色彩：清官政治的不断提倡，反贪的普遍化，甚至举报、上访竟会成为中国法律生活的常态！至于价值观与生活方式方面，各种现代化因素因为中国传统文化这一底色而略显光怪陆离：人的现代化尚没有完成，有些人已提前跃进了后现代；而现代化的外衣之下，又难免露出封建的小来。所有的这一切，导致我们不得不警惕：中国，将走向何处去？<br />
<br />
　　首先需要警惕的是，中国历史上，缺少一场真正意义上的文化启蒙运动，或者说，五四运动后期的转型――从文化转向政治，从科学转向革命――导致中国的启蒙半途而废。启蒙没有完成，中国这样一个人口大国，就恰恰缺少了最不应该缺少的现代化内核――公民及公民意识！无庸讳言，现代化首先是人的现代化，没有人的现代化，其他层面的现代化将注定是空中楼阁！可以说，这种警惕导致了我治史的基本态度，这种态度就是怀疑。怀疑不是否定，而是一种实证精神。相信，就去证实；不相信，就去证伪。笛卡尔说：“怀疑就是方法！”荀子说：“信信，信也；疑疑，亦信也。”我想通过我的努力，传达一种方法，一种态度，一种精神。众所周知，真理不在任何人手中，真理实质上就是个人在面对所谓的真理或所谓的谬误时所应持有的怀疑态度和理性思维以及辨别二者的基本方法！我相信，如果中国的人民群众，都具有了这么一种态度这么一种思维，即便仍没有掌握方法，那么，他们就迈出了走向现代化公民的第一步！有了这么可贵的一步，那么，历史上的某些悲剧应是可以避免的！<br />
<br />
　　其次需要警惕的是，中国历史的悠久，文化的丰富，注定了中国传统文化的弹性与无所不在的影响力，也就是说，传统文化的负面绝不会因为一次革命，一次运动，就能完全改观、消失的。它暂时地消失了，但是一段时间之后，它会折回来；或者，它表面上改观了，但底下的暗流仍在。我把传统文化的这种弹性与隐蔽性概括为“文化的陷阱”。这种陷阱，从政治制度层面来讲，会使张勋复辟，会使袁世凯称帝，更会使不穿龙袍的皇帝君临中国！这种陷阱，从社会生活层面来讲，会使女人继续坚持身体上的或者精神上的裹脚，会使出走的娜拉回头，会使臣民意识、奴性意识继续存留，会使诸多政策与教育“目中无人”，会使人的解放成为一纸空谈！<br />
<br />
　　种种警惕导致了我的写作只能是一种提醒：在我推论出的“陷阱”嫌疑比较大的文化地带，插上“此处事故多发”之类的招牌！这种自觉的意识导致我在写作此书时，对内容有所选择。一句话，注重内容的时效性。比如中国农民、中国传统外交、中国传统知识分子、中国文化中国人、中国贪官与中国清官等内容，无不希望它们对于中国多个领域、多个层面的现代化建设能有更多的警示！<br />
<br />
　　书的内容及风格大都偏向有趣，这跟我的天性有关，大事小事，皆欲情趣化而后快：无趣的人不交，无趣的事不做，无趣的文章不写，无趣的课不讲！记得南怀瑾老先生发过一次牢骚，说他讲孟子，不得不增加许多有趣的故事，否则学生们对中国传统文化不感兴趣。我不是这样，如果讲课无趣了，不待学生有意见，我自己就先有意见了。如果写书无趣了，不待读者诸君有意见，我自己就看不下去了。另外，这还跟我的人生态度有关。我喜欢老庄，庄子鼓盆而歌送亡妻上路的风度，也影响了我对历史的态度。我觉得，惟有大悲之人，才能奏出真正狂欢的音乐。所谓的苍天一声笑，泪在心中流，应该是一种彻悟后的境界吧？当然，爱之深，恨之切，更是人文学者的常态。德国著名的爱国诗人歌德，曾在自己的诗歌中把一重重的“诅咒”送给了他“多灾多难的祖国”。我虽然炼不出歌德那样的“重磅武器”，但是在某些言辞方面，可能还是有些峻切的，这一点，还希望读者诸君多多体谅，爱到深处，便有些情不自禁罢了。</div>

							
							
							
							
