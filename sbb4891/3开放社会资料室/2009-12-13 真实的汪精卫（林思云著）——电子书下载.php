																											<div id="postmessage_371388" class="t_msgfont">　中国经常有这样一种怪现象，提到某个人的名字是妇孺皆知，但问起该人的生平和事迹时，却又是鲜为人知。汪精卫就是这样一个特殊的人物。汪精卫在中国可以说是家喻户晓的大汉奸，可是大家对汪精卫的“卖国事迹”却是一问三不知。这也是中国历史教育的一大特色；只告诉大家他是坏人，却又不告诉大家他作过什么坏事。<br />
　　好在中国学生头脑比较简单，不喜欢刨根究底，所以历史老师还不至遇到太大的麻烦。　<br />
　　汪精卫在中国是一个完全被否定的人物，甚至被认为是中国人的耻辱。因此现在的中国历史教科书中，把汪精卫从辛亥革命和国民党的历史中完全抹杀，只是抗日战争的历史中实在无法抹杀汪精卫的存在，才简单地提到了汪精卫和他的维新政府。<br />
　　这种出于主观愿望而随意修改历史的作法，使中国人对历史产生了错误和虚假的理解，实在有碍于中国的发展与进步。 <br />
　　历史上真实的汪精卫，曾是近代中国有数的叱咤风云的历史人物，对中国的历史走向有举足轻重的影响。在历史走到21世纪的今天，应该考虑还汪精卫的本来面目，重新认识历史上真实的汪精卫。<br />
　　要为汪精卫写一个比较真实客观的小传还有不少困难，最大的困难就是有关汪精卫的资料十分匮乏，人们似乎都极力避开这样一个敏感人物，怕给自己惹来各种麻烦。<br />
　　1980年代后期，中国开始出现一些关于汪精卫的书籍，比如蔡德金的《汪精卫评传》，黄美真的《汪精卫传》，秦亢宗的《走向深渊》等。但中国出版的书都有一个共同的问题，就是有意识地贬低丑化汪精卫，写书的重点放在猎奇和艳史方面，不是严肃的人物传记而近似于野史小说，真实性有很大的疑问。 <br />
