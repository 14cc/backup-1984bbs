5、万府门前绮思（4月16日，日丽风暖，春光无限）<br />
中午随孟 晓苏上他的办公室谈事。孟是万里的秘书，办公室在万里的院子里，院子在中海的西侧，与陈云家的院子相邻。我乘他取材料的时候，打量了一下这座院落。有长长 的回廊，数进院子，一式画栋雕梁的王府式建筑。静谧而安详。孟的办公室前有一株花事极盛的海棠树，是有名的西府海棠，粉白色花，团团簇簇，很少有缝隙。许 多小蜜蜂异常忙碌，上下翻飞，在花蕊上陶醉轻薄。怪不得深闺中怀春的少女面对春花会顿生惆怅，会有无名的忧愁。在这样芬芳的时节，面对如此飘溢著春情的海 棠，你总要停下匆忙的脚步，总要发出长长的叹息，你想得到又想付出很多东西。你想写诗，想动情，想收到几张署有芳名的锦笺，想约<br />
会，想写一封辞藻 华美的信给什么人……总之，你想爱与被爱。如果此时，你什么也得不到，没有人给你写信，也没有人读你的信，那心情……想想林黛玉吧，想想“藏在深闺人不 识”的玉人吧。当然，如果象万里这样的人突然赋闲，独对西府海棠，那晚景，怎堪与外人道？所以，象他这样的人，人生中的重中之重，当然是保权……呵，晓苏 来了。<br />
<br />
（2）前警卫局长的国骂<br />
午餐桌上，退下来的警卫局局长问我们看今天的人民日报没有，上面有白纪年写的一篇文章《清除政治生活中的封建遗毒》。看来，他很反感，又不明说。他又十分愤慨地提到人民日报“大地”副刊，说竟然发表了一篇文章，说封建主义的东西相当多。<br />
“他妈的个混蛋，”他骂道，“发表这样的文章的编辑应当撤职！”<br />
我知道他说的是哪一篇文章，那是上海杂文家冯英子写的，对首长一出动，就前呼后拥，戒备森严，让百姓回避的做法提出批评。<br />
“连起码的事实都不顾，北京地区从82年以来，就没有搞什么戒严了！”他嚷道。<br />
作 为前警卫局长，他的愤怒可以理解。但我想到的是另外一层，他除了关注自己的处境之外，怎么就不多想一想呢？我愈来愈感到，不少在首长身边工作的人员，除了 无条件服从和使人感到有条件的舒服之外，看不出有更多的长处。而这些人，智力比别人低，权力比别人大，这就危险。他们经常随心所欲地辱骂那些天才的作家、 艺术家、理论家和记者，批评他们的作品。他们根本不了解这些精神生产的精微和伟大，细致和复杂。批评是粗暴的，还没有达到理性的最低要求。而他们的批评却 往往影响首长的态度和决策。在目前，批评人，特别是批评有进步思想的人，不会给他们带来多大坏处，还可以表现自己的立场坚定。所以，批评别人成了一种时 髦。<br />
9、疲惫的胡启立和附和的王兆国（4月21日）<br />
下午约3时，启立、兆国召集书记处办公室人员开会，讨论启立的五一讲话稿。全总的两个起草人也出席。我们上午已讨论过，由进玉谈了修改意见。<br />
看 上去，胡启立相当疲惫，昨天刚从上海回京。两眼微红，很少专注地看人。一忽儿埋著头，托著额，似在瞌睡；一忽儿头枕著椅背，两眼斜视天花板。这种作风与传 说中的周恩来的作风不同。周无论在什么场合出现，总是神采奕奕，不露倦容（虽然他很累），而且总是专注地听别人讲话，眼睛深邃地看著讲话的人。我想，作为 一个领导人，在工作的时候，特别是在面对下级时，任何时候都不能显出漫不经心，甚至疲惫不堪的样子。<br />
