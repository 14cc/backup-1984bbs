													<h2>ZT。杨学士与中国的渊源</h2>
						
						
																											<div id="postmessage_56014" class="t_msgfont">2007年7月30日意大利的世界著名导演安东尼奥尼逝世，世界许多地方都纪念他，估计明年欧洲的几个电影节都要想尽办法抢着得到有他作品版权公司或者家属许可举办他的作品回顾展。与世界其他地方略有不同，中国除了一些电影专业人士在总结、回顾他对电影艺术贡献之外，还对他另有一番注目。中国更注重安东尼奥尼、更多谈论他的故去是因为他拍过一个纪录片《中国》。《财经》等几个杂志命我写一些悼念文字。我在总结、清理安东尼奥尼电影艺术成就和他与中国的瓜葛时，发现了一个大科学家的名字──杨振宁。我惊奇的发现，原来这位美籍华裔大科学家不但懂些理论物理、懂些爱情，他还懂电影耶。当年，整个中国掀起批判安东尼奥尼的浪潮并非起于青萍之末，对此片最早有警惕、有所注意并让这种高度的认识和警觉上达天庭的，就是这位大科学家。那他有所警惕以后跟谁打这个关于《中国》的小报告呢？说出来吓你一下，全国人民一直敬爱的周恩来。 <br />
列位看官，这里有故事吧，听我细细道来。 <br />
<br />
1970年11月5日中国和意大利正式建交。1971年7月20日，意大利广播电视公司向中国外交部新闻司发出公函，希望在中国拍摄一部介绍中国的纪录片，提出聘请的导演是安东尼奥尼。我没去考证，估计当时安导演是意共党员。电影圈里有个真实的故事，另一个意大利导演贝托卢奇来中国拍《末代皇帝》时就满世界拿着一张介绍信函到处要求协助，那介绍信上他名字旁边有个括弧注明他是“意共党员”。 <br />
<br />
我估计，当时毛泽东和手下也很急着要让世界其它地方的人们知道无产阶级文化大革命给中国人带来的兴奋状态和“战天斗地”、“改造人的灵魂”的巨大成就。于是好像双方各取所需一拍即合。1972年5月6日，经过中国外交部代部长姬鹏飞和代行文化部职责的国务院文化组批准，由中国驻意大利大使馆文化参赞正式向意大利广播电视公司转交中国的邀请函。1972年5月13日，意大利广播电视公司负责文化节目的主管弗利奥・哥伦布亲任领队，率安东尼奥尼摄制组到达中国。按照中方要求，在22天之内匆勿赶往北京、上海、南京、苏州、河南林县等地拍摄。 <br />
<br />
可是跟很多婚姻一样，双方睡到一起之后并不一定琴瑟相合、如鱼得水。安东尼奥尼对中国刻意安排的拍摄计划和明显的摆拍、安排场景有一些意识，他想了很多招数跟陪同的人周旋。他把这些在解说词里明确说了出来。他自己还说：“‘为了不引人注目，我们掩盖了我们的摄影机。’禁止拍摄的地方，就‘假装停拍’，暗中继续拍摄；而双方商定拍摄的、他们又不感兴趣的地方， ‘就用未装胶片的摄影机空拍’。”（引自陈东林《二十世纪七十年代电影&lt;中国&gt;引起的风波》，载《党史博览》2006年6期） <br />
<br />
1973年1月，在意大利首都罗马举行了电影《中国》的首映发布会。这是西方国家首次和唯一的一次被批准进入“文革”中的神秘中国拍摄的纪录片，有人将它称为“诚实的独白”。影片放映后引起极大反响，美国广播公司花了25万美元购买了它在美国的公开放映权。 <br />
<br />
那后来这个纪录片是怎样被中国高层领导注意，把它当一部反华影片大批特批呢？这就跟我们故事的大科学家主人公有关了。 <br />
<br />
杨振宁是怎样打这个小报告的呢？他是写信给周恩来的吗？非也。实际情况是，这档子事是他当面跟周恩来说的。 <br />
<br />
1973年5月17日夜里，周恩来会见美籍华裔科学家杨振宁。杨振宁对周恩来说：“我不知道周总理是否认识安东尼奥尼，他是意大利很有名的导演。去年来中国访问了很久，后来又拍了一个电影。这个电影我没看过，一般看过的中国人都不喜欢。后来，我看了一个比较左倾的小报纸，上面有一个中国学生的分析，我觉得很深入。这个人大概是个学电影艺术的，他说，这个电影表面上看不错，但如果你对安东尼奥尼过去的电影手法有点了解的话，你就知道他是在恶毒地攻击中国。他举了一个例子，在电影结束时有两个场面，一个是有一群小孩在那里玩，接着是一个木偶戏的场面。他的意思是说，中国的小孩都变成了木偶。”（引自陈东林《二十世纪七十年代电影&lt;中国&gt;引起的风波》，载《党史博览》2006年6期） <br />
<br />
原来，大科学家打这个小报告时还没看到这个电影。不过这也不奇怪，当时全国不知有多少中小学生和工农兵都是压根没看过或者没完整看过这部电影就写了慷慨激昂的大批判影评，我也是其中一个。 <br />
<br />
周恩来当时也不知是没权还是不懂，他没很在意这件事，只是说要查查。1973年12月11日，中央广播事业管理局一位干部就电影《中国》问题给江青和姚文元写了一封举报信，他认为安导演在污蔑中国。姚文元立即批示：“转中组部业务组调查后写一报告。”17日，这名干部又写了第二封信，他在第二封信中说： <br />
<br />
所谓《中国》长片，是完全站在帝国主义立场观点上，极恶毒地诬蔑我国的反动影片，整个影片通篇把我国描写成贫穷、落后、愚昧、灰暗的样子，完全歪曲了我们伟大中国的形象，令人非常气愤。此片在世界上许多国家放映后，对我国影响极坏。举报信中有一句话是重磅炸弹：“为此，美籍中国科学家杨振宁给周总理来信，反映此片很坏”，这句话下面不知被谁重重地画上了道道。（引自陈东林《二十世纪七十年代电影&lt;中国&gt;引起的风波》，载《党史博览》2006年 6期） <br />
<br />
姚文元对此信的批示是：“建议文化组调看影片，并会同中组部彻查此事，共同提出处理意见。”江青的批示是：“应严肃认真对待，并提出处理意见，报中央审批。” <br />
<br />
1973年12月30晚上，全体在京的中央政治局委员调看了《中国》。 <br />
<br />
1974年1月30日，《人民日报》发表了本报评论员撰写的文章《恶毒的用心，卑劣的手法──批判安东尼奥尼拍摄的题为〈中国〉的反华影片》。文章写到： “去年开始在一些西方国家放映的、由意大利导演安东尼奥尼拍摄的题为《中国》的反华影片，就是当前国际上一小撮帝国主义和社会帝国主义分子对新中国极端仇视的心理的反映。这个影片的出现，是一个严重的反华事件，是对中国人民的猖狂挑衅。” <br />
<br />
此后，各种报刊上发表了大量批判文章，一直持续到1975年。仅1974年二、三月间发表的部分文章就结集了一本200页的书出版，名为《中国人民不可侮──批判安东尼奥尼的反华影片〈中国〉文辑》。 <br />
<br />
1974年7月中旬，当毛泽东在政治局会议上提到帽子店，江青说：“帽子店，就是有一个电影叫《中国》，太坏了，但是汉奸帽子还没有戴，因为还弄不清楚是谁搞的。我一定特别注意，主席放心。”（引自同上文） <br />
<br />
在1978年11月10日召开的中央工作会议上，中共中央副主席李先念对有关部门又谈到了这部电影，他说：影片《中国》是有点毛病的，它伤害了中国人民的感情。但“四人帮”利用它来反对周总理，外交部要好好研究一下。 <br />
<br />
安东尼奥尼1972年来拍片子时，我在工厂当童工。1974年对《中国》搞大批判时，我在工厂里被人称为“文豪”之一。我记得，当时是经过选择的批判骨干才可以去看这部影片经过剪辑的版本，而且我们看到的剪辑片段是配上了旁白批判词的，批判词大概是这一类的：请看……，请看……安东尼奥尼却把中国人民描绘成愚昧无知，与世隔绝……他就是这样丑化中国人民……他挖空心思、处心积虑……。 <br />
<br />
我那时很高兴写批判文章，因为厂里把我抽出来去写骂《中国》的文章我就可以有两天不干活。 <br />
<br />
2007年8月7日<br />
