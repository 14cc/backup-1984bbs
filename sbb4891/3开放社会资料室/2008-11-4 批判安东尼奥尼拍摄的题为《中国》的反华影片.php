																											<div id="postmessage_3371" class="t_msgfont">恶霉的用心 卑劣的手法<br />
――批判安东尼奥尼拍摄的题为《中国》的反华影片<br />
《人民日报》评论员<br />
<br />
自从天安门广场上升起五星红旗，新中国宣告诞生的一天起，世界上各种政治力量就对我们国家发生的翻天覆地的社会变革和在社会主义建设中取得的巨大成就，抱有不同的态度。亿万革命人民和广大国际朋友表示赞扬和同情，而一小撮反动势力则表现出极端的恐惧和刻骨的仇恨。这是古今中外一切大革命所必然遇到的一种现象。去年开始在一些西方国家放映的、由意大利导演安东尼奥尼拍摄的题为《中国》的反华影片，就是当前国际上一小撮帝国主义和社会帝国主义分子对新中国极端仇视的心理的反映。这个影片的出现，是一个严重的反华事件，是对中国人民的猖狂挑衅。<br />
安东尼奥尼是在一九七二年春作为我们的客人到中国来的。他带着摄影机访问了北京、上海、南京、苏州和林县。但是，他的中国之行，不是为了增进对中国的了解，更不是为了增进中意两国人民的友谊，而是怀着对中国人民的敌意，采取别有用心的、十分卑劣的手法，利用这次访问的机会，专门搜罗可以用来污蔑攻击中国的材料，以达到不可告人之目的。在他拍摄的长达三个半小时的影片中，根本没有反映我们伟大祖国的新事物、新气象、新面貌，而是把大量经过恶意歪曲了的场面和镜头集中起来，攻击我国领导人，丑化社会主义新中国，诽谤我国无产阶级文化大革命，侮辱我国人民。任何稍有民族自尊的中国人，看了这部影片，都不能不感到极大愤慨。如果容忍这样的影片在世界上招摇撞骗，那就等于承认任意侮辱中国人民的反动宣传是正当的行为，那就是向国际反动派的反华挑衅投降。我们要彻底揭露和批判这部影片的反革命实质，回答国际反动派对中国人民的挑战。这是当前值得人们十分重视的思想和政治战线上的一场严重斗争。<br />
<br />
（一）<br />
<br />
影片摄制者在解说词中说，他“不打算评论中国，而只想开始观察中国的各种面目、姿态和习惯”。这完全是骗人的。这个所谓“纪录片”，每一个镜头都有评论，这是借用反动艺术手法污蔑和丑化中国的极其恶毒的政治评论，是肆无忌惮地公开露骨地反华反共反革命的政治评论。<br />
影片的镜头从北京天安门开始，看起来似乎是很自然的，其实，这种安排却是为整个“纪录片”的反动主题服务的。影片说，“北京是中国政治、革命的中心”，“人民共和国就在天安门宣告成立”，“文化革命的红卫兵浪潮也在这里兴起”。然后，影片“离开天安门”，带领观众开始“观察”中国，也就是要人们看看中国革命究竟给中国人民带来了什么，接着就展开一连串的反动画面，把新中国糟踏得不象样子。影片的这种结构和布局，纯粹是为了把攻击矛头集中地指向中国共产党领导下的革命。咒骂革命，否定革命，反对革命，就是这部影片的要害所在。<br />
<br />
反动影片《中国》全盘否定和完全抹煞了我国社会主义建设各条战线取得的伟大成就，企图使人相信今天的社会主义新中国同往日的半封建半殖民地的旧中国几乎没有什么两样。<br />
<br />
安东尼奥尼把上海作为“工业化的大城市”介绍给观众，其目的恰恰是要污蔑我国的社会主义工业。明明上海有许许多多现代化的大型企业，影片的摄制者却视而不见，而专门搜集设备简陋、手工*作的零乱镜头。明明黄浦江边矗立着能制造万吨轮的造船厂，江上停泊着我国的远洋轮，而在安东尼奥尼的镜头下，黄浦江里的大货船都是外国的，小木船都是中国的。影片的摄制者公然站在帝国主义的立场上说，上海的工业“并非产生于今天”，“上海作为一个城市，则完全是由外国资本在上世纪建立起来的”，而解放后“仓促建立起来的工业往往只比大的手工业工厂好些”，“上海最大的炼油厂，也是一个可怜的厂，几乎是用废料建成的”。这不是明目张胆地大肆炫耀十九世纪帝国主义侵华的“功劳”，竭力贬低我国人民自力更生发展工业的伟大成就吗？影片中还以极其卑劣的手段拍了一些丑化劳动人民的镜头。作者的险恶用心是要暗示，中国的主要工业城市上海尚且如此，其他地区岂不可想而知！<br />
<br />
安东尼奥尼把林县作为“中国的第一个社会主义山区”介绍给观众，其目的恰恰是要污蔑我国的社会主义农村。在影片中，闻名中外的红旗渠一掠而过，既看不到“人造天河”的雄姿，也看不到林县河山重新安排后的兴旺景象。银幕上不厌其烦地呈现出来的是零落的田地，孤独的老人，疲乏的牲口，破陋的房舍……。他大肆渲染林县“农民的贫困”，把一个山村说成是“荒凉和被抛弃的地方”，把一所乡村小学竭力加以丑化。安东尼奥尼还恶狠狠地说，在今天的中国，“如果我们想发现一个农村‘天堂’，那就天真了”。这不是赤裸裸地诬蔑解放了二十多年的中国农村是人间地狱吗？<br />
<br />
这部影片对我国社会主义建设事业的诋毁和污蔑是多方面的。从城市建设到人民生活，从文化教育到体育运动，从医疗卫生到计划生育，以至幼儿园，统统不放过。<br />
<br />
影片完全无视我国城市面貌的巨大变化，渲染北京“依然是个古老的城市”，住屋“非常简陋”，“城市规划化令人失望”；苏州“同它的古老的面貌差异很小”；上海城市面貌的改变，不过是昔日“西方的经济帝国”在租界修建的房子“今天成了办公楼”。<br />
<br />
影片竭力否认我国人民生活条件的显著改善，说什么“北京人是贫穷的，但并不悲惨”。承蒙这位导演先生手下留情，还说我们不算悲惨；但他的真意是嘲笑我们“贫穷”。他不是在城市和乡村拚命捕捉一些镜头，攻击人们“衣服破旧”，“劳动繁重”，诬蔑在中国到处都可以遇到“穷人”吗？所有这些完全是一副帝国主义老爷的腔调！<br />
<br />
安东尼奥尼所以把解放后的中国描绘成这样一团漆黑，一无是处，这也不行，那也不行，无非是要人们得出中国不应当进行革命的反动结论。他攻击人民公社经历了“失望”，胡说无产阶级文化大革命“打乱了生产系统”，使前人遗留下来的古迹已剩下“寥寥无几”，甚至借用打太极拳的镜头，造谣说“新的领导人”要“取消”这一“古老的传统”。总之，在安东尼奥尼这个反动分子看来，中国的社会主义制度不好，中国的革命搞糟了，只有倒退，只有复旧，才有出路。这就充分暴露了挂着“左派”招牌的安东尼奥尼的反革命真面目。<br />
<br />
（二）<br />
<br />
安东尼奥尼为了诋毁中国革命，攻击我国的社会主义制度，在影片中对中国人民的形象和精神面貌进行了令人不能容忍的丑化。他企图通过影片制造这样的假象，似乎中国革命并没有改变中国人民的地位，没有使中国人民在精神上得到解放，而中国人民对社会主义制度也是没有热情的。<br />
<br />
全世界都看到，已经站起来了的中国人民，精神面貌发生了巨大变化。“中国劳动人民还有过去那一副奴隶相么？没有了，他们做了主人了。”在我们的国家，“从来也没有看见人民群众象现在这样精神振奋，斗志昂扬，意气风发。”但是，安东尼奥尼却把中国人民描绘成愚昧无知，与世隔绝，愁眉苦脸，无精打采，不讲卫生，爱好吃喝，浑浑噩噩的人群。为了丑化中国人民，他挖空心思地拍摄坐茶楼、上饭馆、拉板车、逛大街的人们的各种表情，连小脚女人走路也不放过，甚至于穷极无聊地把擤鼻涕、上厕所也摄入镜头。在林县，安东尼奥尼突然闯入一个山村，把摄影机对准那里的群众，当群众反对他这种手段时，他就诬蔑群众的反应是“有的恐惧，有的害怕”，“经常是麻木不仁和毫无表情”。安东尼奥尼大摆其“欧洲人的自傲”的架子，处心积虑地往中国人民脸上抹黑，这是对站起来的中国人民的莫大侮辱！<br />
<br />
更刻毒的是，安东尼奥尼还用拐弯抹角的语言、含沙射影的手法来向观众暗示，中国人民的精神是受压抑的，心情是不舒畅的，对现实是不满的。他在上海城隍庙茶楼的画面中插入了一句不怀好意的旁白：“这里的气氛稀奇古怪”，“既想念过去，又忠于现在”。他在这里说的所谓“忠于现在”不过是一句反话，实际上是要诬蔑中国人民对新社会的拥护是出于被迫的，并非真心实意。安东尼奥尼不是在影片中一再渲染中国人民是不自由的吗？他公然嘲讽工人讨论会的发言“重复而单调”，“不是一个真正的讨论会”，诬蔑中国儿童高唱歌颂毛主席、共产党的“政治性”歌曲同他们的天真可爱不相容，因而并非发自内心。他还胡说由于人们的“谨慎”“使人几乎觉察不到他们的感情和痛苦”。在他看来，中国人民是有着不满现实的莫大“痛苦”的，只是不敢表达出来而已。这完全是一派胡言。在我们这个无产阶级专政的社会主义国家里，人民当家作主，政治局面生动活泼，广大人民享受着真正的民主，心情无比舒畅。安东尼奥尼企图钻空子，煽动中国人民对新中国和社会主义制度的不满，只能是枉费心机。感到“痛苦”的则是一小撮妄想在中国复辟地主买办资产阶级专政的反动派。至于说什么中国人民“想念过去”，这更是污蔑。什么人“想念过去”？中国人民对百年魔怪舞翩跹的“过去”，是深恶痛绝的，只有帝国主义分子及其在中国的代理人，才念念不忘他们失去了的“天堂”，朝朝夕夕梦想使中国倒退到半封建半殖民地的境地。但是，历史的车轮是拉不回来的，任何企图开倒车的人，必将被历史的巨轮辗得粉碎！<br />
<br />
（三）<br />
<br />
安东尼奥尼拍摄影片《中国》所采用的手法，也是极端反动和卑劣的。<br />
<br />
在镜头的取舍和处理方面，凡是好的、新的、进步的场面，他一律不拍或少拍，或者当时做做样子拍了一些，最后又把它剪掉；而差的、旧的、落后的场面，他就抓住不放，大拍特拍。在整个影片中，看不到一部新车床，一台拖拉机，一所象样的学校，一处热气腾腾的建设工地，一个农业丰收的场景……。而他认为可以用来污蔑中国和中国人民的东西，则又是全景，又是特写，不厌其冗长。影片在拍摄南京长江大桥时，故意从一些很坏的角度把这座雄伟的现代化桥梁拍得歪歪斜斜，摇摇晃晃，还插入一个在桥下晾裤子的镜头加以丑化。影片关于天安门广场的描绘更是十分可恶。它不去反映天安门广场庄严壮丽的全貌，把我国人民无限热爱的天安门城楼也拍得毫无气势，而却用了大量的胶片去拍摄广场上的人群，镜头时远时近，忽前忽后，一会儿是攒动的人头，一会儿是纷乱的腿脚，故意把天安门广场拍得象个乱糟糟的集市，这不是存心污辱我们伟大的祖国吗！<br />
<br />
在影片的剪接上，画面跳来跳去，好象是东拉西扯，杂乱无章，其实每一个片段的衔接，都是恶意安排的。比如，影片拍摄者先是向观众介绍十三陵地下宫殿陈列馆中反映明朝劳动人民受压迫和进行反抗的泥塑群象，讲述当时农民的生活是如何的悲惨，然后镜头一转，就出现一队青年学生扛着铁锹下乡参加劳动的情景，再转到中阿友好人民公社，用一位女社员在劳动中擦汗的镜头，来宣扬什么“日常的田间劳动是劳累繁重的”，并且叫嚷中国农村没有“天堂”！这种手法分明是影射今天中国农民的境遇比几百年前封建社会的农民好不了多少。<br />
<br />
影片在光线和色彩的运用方面也是很坏的。影片的大部分都以灰暗的光线和阴冷的色调拍摄。黄浦江象笼罩着浊雾，北京的街道被抹上一层青光，林县的山村阴影重重。总之，许多画面给人以惨淡、凄凉、阴郁、冷酷的印象。尤其恶劣的是，影片摄制者还利用配乐作为进行诽谤的手段。他在影片中没有拍过我国一个革命样板戏的镜头，却拿样板戏的一些唱段肆意嘲弄。当响起《龙江颂》中江水英唱“抬起头，挺胸膛”时，画面上出现的竟是猪摇头的动作。据有关单位揭露，这种剪接完完全全是伪造的。这是蓄意污蔑我们的革命样板戏，攻击我们的文艺革命，真是恶毒透顶！<br />
<br />
安东尼奥尼对中国人民怀有敌意，这从他在中国的拍摄活动中也可以得到证明。他在解说词中公开宣扬有很多镜头是象间谍那样偷拍的。他得意地说，他在黄浦江如何“冲破禁令偷偷地拍摄了”中国军舰；在北京的王府井大街如何“把摄影机掩盖住”，“冷不防地抢摄这里的生活场面”。他还埋怨说“带着摄影机在前门大街那里行走是不方便的”。什么不方便？就是做贼不方便。更有甚者，他为了炮制污辱中国人民的镜头，在北京中阿友好人民公社，竟然要别人制造社员打架的场面，让他拍摄；在另一个场合，他还要群众按照他的口味改换服装，否则拒绝拍摄。他的这种偷拍、强摄、作假的行径，本身就是对中国人民的极大的不尊重和藐视。<br />
<br />
（四）<br />
<br />
反华影片《中国》在国际舞台上的出现，决不是一个偶然的孤立的事件，而是有它的国际背景的。<br />
<br />
近些年来，我们面临的国内外形势越来越好，毛主席的革命外交路线取得新的更大胜利，我国的国际影响日益扩大。帝国主义和社会帝国主义妄想孤立中国、颠覆中国的阴谋遭到可耻的破产。但是，我们的敌人对于他们在中国的失败是不会甘心的。攻击中国革命，污蔑社会主义新中国，就是他们妄图在中国实现反革命复辟，使中国重新沦为殖民地半殖民地的一种舆论准备。<br />
<br />
人所共知，苏修叛徒集团是国际上反华的急先锋和总后台。从赫鲁晓夫到勃列日涅夫，都使出浑身解数，对中国人民极尽污蔑、攻击之能事。他们说，中国人穷得喝大锅清水汤，连裤子都没得穿；无产阶级文化大革命使中国的生产力遭到“新的破坏”；中国人民“疲困不堪”，处在“严重的苦难”之中，过着“兵营式的生活”，等等。可是，所有这些愚蠢的诽谤，只是暴露了苏修叛徒们的丑恶面目，而没有使他们捞到任何东西。在今天的世界上，苏修的反华谎言已经没有多少市场了。正是在这种情况下，安东尼奥尼的反动影片披着“客观”、“真实”的外衣来欺世惑众，又把苏修这一套造谣诬蔑搬出来，妄图起苏修反华宣传所不能起的作用。安东尼奥尼实际上不过是充当了已经破产的苏修反华宣传的应声虫。<br />
<br />
安东尼奥尼的反华影片《中国》出笼之后，美国广播公司花了二十五万美元重金购进这部影片，并在美国公开放映，还有人帮腔说这部反动影片是“吸引人的”。看来，杜勒斯的阴魂仍然在一些美帝国主义分子的身上，安东尼奥尼的反华影片的出现，也适应了这些反动势力的需要。<br />
<br />
安东尼奥尼是一个意大利人，但是，他决不能代表千百万对中国人民抱着友好态度的意大利人民。反华，这是广大意大利人民决不会同意的。安东尼奥尼拍摄这样的反华影片，同意大利人民和中国人民要求加强友好关系的愿望，显然完全背道而驰。<br />
<br />
中国人民一贯主张发展同各国人民的友好往来和互相了解。在同各国人民的交往中，我们从不强求别人接受自己的观点。我们一再指出，中国还是一个发展中的社会主义国家，尽管我们的社会主义革命和建设已经取得巨大成就，但是从不隐讳我们的国家还存在着前进中的缺点，还有落后、反动的东西，需要继续革命。毛主席经常提醒我们，要反对大国沙文主义。我们欢迎各国朋友对我们的工作提出批评。但是，谁要是冒充“朋友”而实际上却干着卑鄙的反华勾当，去博取极端仇视中国的帝国主义和社会帝国主义的喝采，我们就要给予彻底揭露，以剥夺其招摇撞骗的资本。只有这样做，才有利于各国人民的互相了解和友好往来。<br />
<br />
安东尼奥尼的反华影片告诉人们，在目前国际国内的大好形势下，必须继续保持清醒的头脑，任何时候都不要忘记，世界上总有敌视中国人民的势力，还存在着尖锐复杂的斗争。这是不以人们意志为转移的。当然，反华也没有什么了不起。一切反华的好汉，不管是大人物还是小人物，不管使用什么武器，玩弄什么手法，都只能是搬起石头打自己的脚。中国人民将坚定地沿着社会主义道路奋勇前进。正如我们伟大领袖毛主席早就说过的：“让那些内外反动派在我们面前发抖罢，让他们去说我们这也不行那也不行罢，中国人民的不屈不挠的努力必将稳步地达到自己的目的。”<br />
<br />
转自 《人民日报》1974年1月30日</div>

							
							
							
							
