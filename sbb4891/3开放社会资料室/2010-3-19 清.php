早7点出发，晚9点回家，先后转战5家旧书店，其中本人“团购”旧书上百本，共支付国币800大元，平均每本书8―10元，与时下的旧书行情基本合拍，也基本处于俺的心理承受限度内。 <br />
<br />
全部旧书中，最难得是一本叫《狂飙曲》的小册子，这是1968年武钢的红卫兵为纪念它们在1966年9・13事件中伤亡的数百名兄弟、创作的一部诗集。如果中国有一天建立“文革博物馆”，我愿将其捐赠，从而让红卫兵的遗孽――左愤，有机会缅怀它们前辈的英雄事迹。 <br />
<br />
新中国成立后出版的革命书籍，具备史料与文学双重价值的，当数《红旗飘飘》系列。此前曾零星买过文革前的单行本与改革开放后的翻印本。但却不知道当年就有精装本发行，昨天有幸买到1961年第一版的《红旗飘飘》精装本第2册与第6册，每本20元，窃喜良久。 <br />
<br />
在昨天的战利品中，有一本绿色封面的有雷锋图像与“雷锋日记”字样的笔记本。扉页上盖有一枚“武汉地区大专院校田径运动会・1976”印章，内有雷锋彩图多张。难得的是笔记本一字未写。当年这样的笔记本稀松平常，现如今却让人滋生出怀旧之情。 <br />
<br />
随着年龄的徒然增长，变得喜欢读回忆录，大凡见之必买。在昨天买进的此类书籍中，《冈村宁次回忆录》、《在华五十年――司徒雷登回忆录》、《使美八年纪要――沈剑虹回忆录》，都有读之而后快之感。 <br />
<br />
一书店业主说有本书不准网购，问我买不买？一看原来是曾经大名鼎鼎的《河殇》，河南美术出版社1988年9月第一版。随手翻看，才知道当年《河殇》的摄制顾问中，有一个人叫厉以宁。店主要价5元，为日后批判用，决定买下。 <br />
<br />
纯资料性书籍读起来是很费时间的，然而，对一点五这样热情于训练国产左愤的良民，手中又不能不掌握些把柄，于是咬牙买下不少，如《我国代表出席联合国有关会议文集・1972》、《康雍乾间文字之狱》、《明实录类纂・自然灾异卷》、《一六八九年的中俄尼布楚条约》、《武昌起义档案资料选编》（上、中）、《五四爱国运动》（上、下）、《华北治安战》（上）。<br />
<br />
读旧书长知识，长智识则必须读新书。用40元买下的一本打折新书《路漫漫――香港独立漫画25年》，回家后一气读了十几页，大吃一惊，香港文化人的素质、水准、追求与境界远在我的想像之外。<br />
<br />
本来还有些书也可买下，但鉴于日益增长的阅读需要与落后的购买力之间的矛盾，只得作罢。</div>

							
							
							
							
