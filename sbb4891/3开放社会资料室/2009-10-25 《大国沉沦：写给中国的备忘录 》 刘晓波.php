													<h2>《大国沉沦：写给中国的备忘录 》 刘晓波</h2>
						
						
																											<div id="postmessage_300124" class="t_msgfont"><a href="http://www.chinesepen.org/Article/hyxz/200910/Article_20091022151223.shtml" target="_blank">http://www.chinesepen.org/Articl ... 0091022151223.shtml</a><br />
<br />
<strong>余&nbsp;&nbsp;杰：文字收功日，中国民主时――《大国沉沦：写给中国的备忘录》导读</strong><br />
<br />
<img src="http://i33.tinypic.com/dr8qgx.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
《大国沉沦》封面<br />
<br />
受刘晓波先生的妻子刘霞女士的委托，我着手为刘晓波先生编辑这本政论文集。就在编辑的过程中，中共当局宣布正式逮捕刘晓波先生。此时此刻，我的心情尤为沉重。因为这样的编辑工作，本应由作者亲自完成，此前刘晓波先生出版的所有作品，都是他自己编辑完成的。而由我来为失去自由的刘晓波先生完成这项工作，我深感不配，亦深感荣幸。<br />
<br />
生活在这样的时代、这样的国族，我从来没有多少幸福感，相反却有十足的耻辱感。因为有刘晓波先生和那么多靠着良知的引导而生活的同胞身陷于囚牢之中，而下流人却悠然上升。如今，刘晓波先生被羁押的北京市看守所，就在离我家仅仅一箭之遥的地方，他在放风的时候，甚至可以望见我家的楼宇。他失去了自由，我却仍然享有自由，我为此而感到羞愧。同时，我更为与刘晓波先生身处同一时代及国族之中而感到巨大的光荣，正是因着他和身边的一群朋友的存在，中国才不至于像圣经中那些邪恶之城一样，因为连一个义人也找不出来，而被上帝所毁灭。<br />
<br />
此时此刻，我不禁想起了二十年之前，只有十六岁的我，在六四的枪声中读到刘晓波的文字时的激动与震撼――那是一本当局在六四镇压之后炮制的《刘晓波其人其事》，由中国青年出版社出版发行，首印即达五万册。该书的前言说：“选编在这本小册子里的刘晓波的言论，堪称为反动狂人写下的反共奇文，我们愿和读者诸君共同赏析、批判，以期把这些毒草连根铲除，化作肥料，用来壮大、巩固我们的社会主义精神文明阵地。”除了大量的批判文章之外，编辑还以附录的形式收入刘晓波的几篇文章“立此存照”。然而，那几篇出自刘晓波之手的酣畅淋漓的文章却吸引我如饥似渴地阅读，差不多每一行都被我用红笔勾画出来。如今，中共当局真该如学者余世存所建议的那样，重版该书，继续对刘晓波的批判。可惜，胡锦涛连这样做的勇气都没有。此时此刻，我也不禁想起十年之前，第一次与刘晓波会面的时候，刘霞在一旁看着我们两个口吃者吃力地交谈，一边观察一边窃笑――那时，我初出茅庐，书商对我的“包装”，使用了当年人们形容刘晓波的那个褒贬交错的词语“黑马”，而当我与真正的“黑马”见面的时候，许多话却无从说起。但从此以后，我们便成了无话不谈的好朋友。<br />
<br />
尽管自古以来先知在故乡都是不受欢迎的，但从来没有哪个时代和哪个国族，像今天的中国这样以折磨和羞辱先知为“志业”。正像艺术家高氏兄弟为刘晓波画的那幅巨大的肖像一样，只有隔着相当的距离，你才能看出刘晓波的脸庞与神色，一旦走近去看，便是模糊一片。刘晓波的价值并不为同胞们所知，刘晓波的存在更是激起了独裁者的仇恨。本来根本没有人想颠覆他们的政权，他们却日夜杯弓蛇影，坐立不安：你不是试图搜集四川大地震中死难学生的名单吗，这就涉嫌“颠覆国家政权”了；你不是试图为死于有毒奶粉的孩子的家长提供法律援助吗，这就涉嫌“颠覆国家政权”了；你不是试图帮助因卖血而染上艾滋病的农民兄弟讨一个“说法”吗，这就涉嫌“颠覆国家政权”了；你不是试图揭露无数的母亲被计生人员强迫堕胎的惨剧吗，这就涉嫌“颠覆国家政权”了。这个国家，这个政权，怎么像一个纸糊的房子那样无比脆弱呢？<br />
<br />
中共当局投资数亿拍摄了一部自我歌功颂德的电影《建国大业》，却将“刘晓波”当作互联网上重点屏蔽的词语。据说，大肆羞辱国民党的《建国大业》，将在台湾的院线同步上映。这个消息并不让我吃惊。这些年来，最爱中国的，不是中国的百姓，而是台湾的明星、商人、文人和政客。在台湾早已过气的李敖，在中国被奉若上宾，终于找到了他的“第二春”，戏子的本性总是去讨好强者。而台商在中国可以雇佣奴隶劳工，可以包养二奶，日子过得比在台湾滋润千百倍，当然“乐不思台”了。戏子和商人如此，政客又怎么会有原则呢？连战、宋楚瑜等明日黄花式的政客，也争先恐后地登陆了――“老大哥”派发的棒棒糖很甜很甜啊，连祭拜祖坟都由中共来买单呢，反正中共不用向纳税人汇报国家财政是如何开支的。在“连爷爷”、“吴伯伯”之后，会不会再有一个“马叔叔”呢？既然反攻大陆已成遥不可及的春梦，那么退而求其次，当个南唐李后主也不错，卧榻之旁，就容我安睡一会儿吧！如今，全世界不都在争先恐后地向中共献媚吗：法国总统萨科齐因为会见达赖喇嘛，而在中共的铜墙铁壁上碰得头破血流；美国总统奥巴马被经济危机搞得焦头烂额，舍弃人权外交而对华绥靖…… 这是大势所趋，识时务为俊杰啊。君不见，“北京共识”隐然取代“华盛顿共识”，上海合作组织似乎要挑战北约组织；君不见，在辉煌的奥运会之后，是更为辉煌的六十年庆典的阅兵式，这是怎样一种万国归心的大国气象啊。<br />
<br />
可惜，这是一个“金玉其外、败絮其中”的大国，这是一个外表崛起、精神沉沦的大国，刘晓波一针见血地指出了真相： “在这样的‘经济奇迹’背后，是制度腐败的奇迹、社会不公的奇迹、道德沦丧的奇迹、挥霍未来的奇迹，不仅是经济成本和人权成本极为高昂的奇迹，更是社会综合成本难以估量的奇迹。”这个时代，与时俱进者多，扛起闸门者少。当讨好中共变成一种世界范围内的“共识”的时候，刘晓波却如同二十年前那样，矢志不渝地站在批判中共的最前线。他用上千篇评论文章，揭穿了“大国崛起”背后的真相乃是“大国沉沦”。如今，中共当局对刘晓波的审讯围绕这些文章展开，这些文章能成为罪证吗？相信每一个认真阅读过这本文集的读者都会有公正的答案。刘晓波的说法一点都不夸张，这样一个恐龙般张牙舞爪的大帝国，对国内民众肆意施暴，对国际社会则输出“精神鸦片”。对此，学者秦晖分析说：“除了低工资、低福利的传统优势外，中国更以‘低人权’的‘优势’人为压低人力、土地、资金和非再生资源的价格，以不许讨价还价、限制乃至取消许多交易权利的办法‘降低交易成本’，以压抑参与、漠视思想、信仰和公正、刺激物欲来促使人的能量集中于海市蜃楼式的单纯求富冲动，从而显示出无论自由市场国家还是福利国家都罕见的惊人竞争力，也使得无论采用‘渐进’的还是‘休克疗法’的民主转轨国家都瞠乎其后。”此种与自由、民主、宪政等普世价值背道而驰的“精神鸦片”，比金三角和中南美洲流出的毒品更加可怕。这一创造了“中国崛起”奇迹的“中国模式”，正在如同癌细胞般大肆扩张，其结果必然是“坑了近百年来人类文明的成果，让世界重新回到原始积累的时代去了”。<br />
<br />
乌云有时确实会遮住太阳的光芒。然而，这个世界上偏偏就是有像刘晓波这样不识时务的人。他从不自我英雄化，而以一颗平常心来从事自己所选择的事业，不管旁观者是掌声还是唾沫。二十多年来，他一如既往地螳臂挡车，一如既往地如索尔仁尼琴那样“牛犊顶橡树”，一如既往地如堂吉诃德那样冲向风车，一如既往地如西西弗斯那样推着石头上山。六四屠城前夕，人们纷纷逃离中国，唯有他毅然中断在美国讲学的计划，只身返回危城，在天安门广场上坚守到最后一刻，并冒着生命危险与戒严部队谈判，终于避免了更大规模的屠杀的发生。此后二十年，他被剥夺教职和在国内公开发表作品的权利，长期遭受秘密警察的监控和骚扰，先后四度入狱，却一直奋笔疾书。二十年间，中国发生的所有重大事件，他几乎都及时发表振聋发聩之评论。在他的笔下，没有任何“敏感”的领域。如同当年的梁启超一样，他毅然放弃创作“传世之作”的个人雄心，转而写作一篇篇看似短命的“觉世之作”。其政论作品，质量之高、数量之多、范围之广、论述之深，当今华人世界无人可及，堪称“第一政论家”。写作《红楼梦》或《管锥篇》，是许多文人学士的人生愿景，但对刘晓波来说，既然生活在一个人权受到践踏的时代和国族之中，此种选择便过于奢侈和贵族化。他并不是缺乏写作《红楼梦》或《管锥篇》的才华，不写乃是出于“不忍”――“不忍”旁观天安门母亲的眼泪，“不忍”坐视孙志刚们的灾难，他决定为天安门母亲和孙志刚们写作。即便在身陷牢狱的时刻，他也从来没有离开过，他始终“在场”。当作家和学者们普遍被招安而与权贵共舞之时，他的存在越发彰显出精神标杆的意义。<br />
<br />
刘晓波不仅坐而论道，且起而行道。从上个世纪九十年代初起，他便起草和组织了多份人权呼吁书，直至最近一份导致中共再度对他下黑手的《零八宪章》。他关怀天安门母亲群体，帮助许多受迫害的维权人士和他们的家人，虽然多年来只有在海外发表文章的微薄的稿费收入，但他对那些需要帮助的人永远比对自己慷慨大方，他帮助的许多人甚至都是从未谋面的人。刘晓波还积极参与公共事务，他是独立中文笔会的创始人之一并多年担任会长、理事，为之付出无数的时间和精力，其中有不少是“鸡毛蒜皮”的小事、杂事以及中国人特有的人际关系方面的协调工作。我做副会长配合他的工作的时候，几度因为他人的恶意攻击而灰心丧气。他便勉励我说，这就是做公共知识分子需要付出的代价，即便是恶意的辱骂也要有相当的心理承受力；一个作家不能只是将个人的写作看得无比重要，尤其是在民主制度尚未建立的中国，需要有一批人为公共事务付出时间和精力。正是在刘晓波的领导之下，独立中文笔会在短短几年间便发展成为中文世界里最积极捍卫言论自由和新闻出版自由的NGO组织，在一定程度上突破了中共对公民结社自由的限制。<br />
<br />
刘晓波是中国最具独立性的公共知识分子之一。“独立”二字，知易行难。如鲁迅所说，不做官的帮忙和帮闲，不做商的帮忙和帮闲，相对来说容易一些；但不做大众的帮忙和帮闲，却是一个更大的挑战。许多异议作者和人权活动人士都有登高一呼、应者云集的梦想，都有充当英雄豪杰和救世主的情结，为此不惜扭曲自我以迎合群众，期待群众拥戴为王。他们从《易经》中算出共产党垮台的日子，以“影子总统”和“影子主席”的名义发布命令、封官许愿。而经历了当年广场运动的刘晓波，深知“广场效应”之迷思，深味激进主义之危害，更对权力对人性的戕害有清醒认知，故而一边批判拒绝改革的中共当局，直陈权力与体制之不公不义；一边毫不留情地指斥那些鼓吹暴力革命、将百姓的生命当作炮灰以达成个人野心的“草莽皇帝”――此种人士在中国的历史与现实中一直层出不穷。如果说中国有一个“反对派”的话，刘晓波乃是其中少数将仇恨化为仁爱的人，这种仁爱甚至扩展到那些监视他、骚扰他的秘密警察身上。刘晓波曾给我讲过一个故事：有一次，他到朋友家做客，特务跟踪他到楼下。他与朋友聊天聊了很久，忽然接到特务打来的电话：“刘老师，我憋了半天尿，楼下找不到公共厕所，你能让我上楼来用一下厕所吗？”刘晓波征求主人的同意之后，遂让这名可怜的特务上来使用了卫生间。是的，受迫害者不能将自己降低到加害者的道德水准上、甚至用“以毒攻毒”来自诩；受迫害者而应当以人格的力量唤起加害者内在的良知。<br />
<br />
刘晓波在今天的中国所扮演的角色，类似于晚清时的梁启超。学者邵建在《看晚清三方角力》一文中指出：在梁氏等人看来，革命既然诉诸暴力，即使成功，国家也会陷入长期的内乱和争斗，收拾动乱的人，必定是有极大本领和权术的独裁者。这正是辛亥以后的情形：从袁世凯到蒋介石再到毛泽东，历史轨迹被梁氏不幸而言中。所以，梁氏在反革命的同时，力推君主立宪，不是推翻清政府，而是以开议会的方式改造它。但他面对的困境是：不在于他反革命，也不在于孙文闹革命，而在于清政府天天制造革命。他两面作战，一边与同盟会战，阐释自己为什么反革命；一边与清政府战，斥责其不断制造革命。最终，还是未能让中国避免暴力革命。一九三六年，中国的内乱方兴未艾，外敌虎视眈眈，梁启超已去世多年。一位笔名叫素痴的读者在《大公报》上缅怀梁氏，认为梁启超对流血的恐惧，“使他不得不反革命”，但他的悯世之心又“使他无法容忍现实政治的黑暗”。于是，两边陈词，既劝革命党放弃暴力，又劝清政府彻底改革。结果，两边都是“言者谆谆，听者藐藐”，以致局势日下。这位素痴先生已是三十年后读梁氏此论，不禁“太息痛恨于盖世雄文的浪掷”。近年来，刘晓波既遭到中共当局的打压乃至系狱，也遭到海内外激进分子和野心家的攻击与辱骂，简直就是梁启超当年遭遇的翻版。那么，今天的中国会重蹈清末的覆辙吗？<br />
<br />
我没有那么悲观。今天的中国，很多地方与清末又大不一样。全球化的突飞猛进，让中国无法像过去那样闭关锁国；信息时代悄然来临，中国网民的人数已达三亿多，超过了美国的总人口；城市化的推进、公共空间的拓展以及民间舆论的崛起，已成不可阻挡之趋势。刘晓波虽然被当局关押，他的文字却通过网络启蒙了千千万万饥渴慕义的心灵（仅在国内需要“翻墙”才能上去的博讯新闻网上，刘晓波个人文集中七百多篇文章的点击率，就高达四百多万，是一百多名海内外异议作家中点击率最高的）；刘晓波虽然暂时失去了自由，但他的思想和文字无时不刻地在形塑着中国未来之走向，在近代以来的中国，以文字改变一个时代的风气与人心，功劳最大的当推梁启超与刘晓波二人。当年，胡适之曾经这样评价梁启超说：“梁任公为吾国革命第一大功臣，其功在革新吾国之思想界。十五年来，吾国人士所以稍知民族思想主义及世界大势者，皆梁氏所赐，此百喙所不能诬也。……近人诗‘文字收功日，全球革命时’，此二语惟梁氏可以当之无愧。”梁氏以其“惊心动魄、一字千金”的文字，带动了一个时代和一个国族的启蒙运动，胡适之的这番表彰并不为过。但胡适之将梁氏与革命牵扯在一起，恐怕是对其最大的误读。而我坚信，从梁启超到刘晓波的思想谱系和文字生涯，贯穿始终的，是对真理的追求、对人权的坚守和对同胞的大爱。文字是有力量的，尤其是背后有道义和良知支撑的文字，对这一点我们不必妄自菲薄，否则掌权者为何害怕梁启超和刘晓波们呢？在此意义上，我们若要衡量刘晓波对当代中国思想文化及政治进步的贡献，不妨套用并稍稍改动那句诗――“文字收功日，中国民主时”。<br />
