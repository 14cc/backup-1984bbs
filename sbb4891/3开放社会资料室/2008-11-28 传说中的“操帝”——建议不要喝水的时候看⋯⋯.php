（十三）重视妇女和老年人的体质与健康问题，积极支持他们参加体育健身活动。注意做好劳动强度较大、余暇时间较少的女职工的体育工作。加强对老年人体育健身活动的科学指导。 <br />
<br />
（十四）广泛开展残疾人体育健身活动，提高残疾人的身体素质和平等参与社会活动的能力。丰富残疾人体育健身方法，培养体育骨干，提高残疾人体育运动水平。 <br />
<br />
（十五）积极为知识分子创造体育健身条件，倡导和推广适合其工作特点的体育健身方法，重视对中高级知识分子进行健康检查和体质测定工作。 <br />
<br />
四、对策和措施 <br />
<br />
（十六）把推行全民健身计划纳入国民经济和社会发展的总体规划，坚持群众体育与竞技体育协调发展的方针，以普遍增强人民体质为重点，加强领导，统筹规划，切实抓出成效。 <br />
<br />
（十七）加强宣传工作，形成全民健身的舆论导向，增强全民体育健身意识，提高对全民健身工作的重视程度。使全社会认识到，身体素质是思想道德素质和科学文化素质的物质基础，全民健身工作是社会主义精神文明和物质文明建设的重要内容，体育发展水平是社会进步与人类文明程度的一个重要标志。 <br />
<br />
（十八）加强群众体育的法制建设，认真执行现有体育法规，有计划地制定并实施社会体育督导、群众体育工作、体育社团、场地设施管理等方面的法规制度。 <br />
<br />
逐步完善群众体育运动竞赛制度，加强对工人、农民、少数民族、残疾人以及各类学生运动会等的组织和管理。突出群众体育运动会和竞赛活动的群众性、健身性、民族性、趣味性和科学性。 <br />
<br />
（十九）充分发挥各群众组织和社会团体在开展群众性体育活动中的重要作用，建立健全行业、系统体育协会和其他群众体育组织，逐步形成社会化的全民健身组织网络。 <br />
<br />
（二十）体育部门要改善资金支出结构，逐步增加群众体育事业费在预算中的支出比重。鼓励企、事业单位、社会团体、个人资助体育健身活动。提倡家庭和个人为体育健身投资，引导群众进行体育消费，拓宽体育消费领域，开发适应我国群众消费水平的体育健身、康复、娱乐等市场。 <br />
<br />
（二十一）实施体质测定制度，制定体质测定标准，定期公布全民体质状况。 <br />
<br />
实施《社会体育指导员技术等级制度》，加强社会体育骨干队伍建设。 <br />
<br />
（二十二）推广简便易行和适合不同年龄、性别、职业特点与体质状况的体育健身方法。挖掘和整理我国传统体育医疗、保健、康复等方面的宝贵遗产，发展民族、民间传统体育。 <br />
<br />
（二十三）加强人民体质与健康的科学研究和技术开发。要发挥体育科技队伍的作用，体育科研单位和体育院校要以群众体育和全民健身的科学研究为重点，要增加对群众体育科学研究的投入，加快科技成果向群众体育健身实践的转化。 <br />
<br />
（二十四）体育场地设施建设要纳入城乡建设规划，落实国家关于城市公共体育设施用地定额和学校体育场地设施的规定。任何单位和个人不得侵占体育场地设施或挪作它用。各种国有体育场地设施都要向社会开放，加强管理，提高使用效率，并且为老年人、儿童和残疾人参加体育健身活动提供便利条件。 <br />
<br />
五、实施步骤 <br />
<br />
（二十五）本纲要采取整体规划，逐步实施的方式。从现在起到2010年分为两期工程。第一期工程自1995-2000年，分为三个阶段：1995－1996年为第一阶段，进行宣传发动和改革试点，初步掀起一个全民健身活动热潮。1997-1998年为第二阶段，通过重点实施、逐步推进，形成崇尚健身、参与健身的社会环境和社会风气。1999-2000年为第三阶段，全面展开全民健身计划的各项工作并普遍取得成效，建立具有中国特色的全民健身体系的基本框架。 <br />
<br />
第二期工程自2001-2010年，经过十年的努力，把全民健身工作提高到一个新的水平，基本建成具有中国特色的全民健身体系。 <br />
<br />
（二十六）本纲要在国务院领导下，由国家体委会同有关部门、各群众组织和社会团体共同推行。国家体委负责组织实施。 <br />
<br />
各级地方人民政府及其体育行政部门应根据当地具体情况，制定本地区的规划和实施方案。各部门、各系统也应制定相应的规划和实施方案。 <br />
<br />
中国人民解放军和人民武装警察部队可根据本纲要的要求，结合部队实际参照执行。</div>

							
							
							
							
