<br />
（一）建国40多年来，我国体育事业取得了很大成就。群众性体育活动蓬勃开展，参加体育活动的人数不断增加，人民体质与健康状况有了很大改善，全民健身工作日益受到社会的重视和支持，群众性体育活动的内容和形式更加丰富多彩，群众体育健身的物质条件逐步得到提高，体育在提高人民整体素质，促进社会主义精神文明和物质文明建设方面发挥着越来越显著的作用。 <br />
<br />
（二）当前，我国经济建设和社会发展对人民的整体素质提出了新的更高要求。但是，全民健身工作的现状还不能适应社会主义现代化建设的需要。群众的体育健身意识还不够强，群众性体育活动的开展还不够广泛，经常参加体育锻炼的人数还不够多，现有体育场地设施在向社会开放、满足群众开展体育锻炼的需要方面还有较大差距，全民健身工作的科学技术和监测管理还比较落后，有关的法规制度还不够完善，适应社会主义市场经济体制的全民健身管理体制和运行机制还在探索之中。这些问题，应随着经济和社会事业的发展，逐步加以解决。 <br />
<br />
（三）为进一步增强人民体质，适应我国社会主义现代化建设的需要，必须采取切实有效的措施，推行全民健身计划，发展群众体育。 <br />
