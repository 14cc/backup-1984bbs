													<h2>1949年前，中国不存在封建主义大山</h2>
						
						
																											<div id="postmessage_482152" class="t_msgfont">1949年前，中国不存在封建主义大山<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;王书瑶<br />
<br />
现在讨论被中共推翻的压在中国人民头上的第二座大山：封建主义。<br />
什么叫封建？封王建国和封侯建邦才叫封建。如果没有这个条件，就不能叫封建。<br />
《现代汉语词典》说，有地主和农民的社会就叫封建社会，这是非常无理的、违背常识的，如果有地主和农民的社会就是封建社会，那么有领主和农奴的社会该叫什么社会？<br />
那么，有地主和农民的社会该叫什么社会呢？<br />
按着已往历史学家对人类社会的分期，是原始社会、奴隶社会、封建社会、资本主义社会（这里在资本下面多了个‘主义’两个字，已经不对仗了，按着对仗的说法，该是资本社会）、社会主义社会（这里又有了个‘主义’）、共产主义社会。<br />
但是，这种对社会的分期，总是带有马克思意识形态的烙印，而且是单纯根据欧洲的情况进行划分的，它并不能代表全世界所有国家的社会发展历史。<br />
中国的情况与欧洲的情况有极大的不同，我有一篇网文，单独对比了中国社会与欧洲社会的区别，有十一个重大不同之处，这篇网文就附在本文之后。<br />
欧洲之所以被称为封建社会，是因为欧洲有领主与农奴，而中国自秦以来，就不再有领主和农奴，所以也就不再是封建社会。<br />
那么，主要由地主和农民构成的中国社会，应该叫什么社会呢？<br />
我以为，按着过去社会学家或历史学家的命名惯例，比照奴隶社会的命名原则和方法，应该叫“农民社会”。奴隶社会之所以叫奴隶社会，是因为当时的社会，主要是由奴隶和奴隶主构成的，并且以此为特征，而秦以后的中国社会则主要是由农民和地主构成的，所以就叫农民社会。<br />
按着这个序列，人类社会的发展，应该是：原始社会、奴隶社会、农奴（封建）社会和农民社会、资本社会和权贵资本社会、公民社会。<br />
权贵资本社会就是中国经过暂短的“暴力空想社会主义社会”，从农民社会进入了权贵资本社会。<br />
公民社会，则是在资本社会高度发展后进入的社会，在政治上，政府与政策由全体公民一人一票决定；在经济上，人人都可以购买股票，人人既可以是工人，也可以是资本家，也就是经常说的“人民资本主义”。<br />
<br />
我们现在最惯常的说法是，在中国农村中有封建地主，我们主要就来讨论中国农村中的地主，是不是封建的？<br />
秦始皇“令黔首自实田”，开创了中国土地私有的历史，结束了“普天之下，莫非王土”的土地君王所有的时代，农民可以有自己土地的时代，也就是说，君主不分封土地，这种状况，一直延续到清代。<br />
在秦以后的两千多年的时间里，有几个不识时务的皇帝，也曾封过诸侯，结果，不是造反就是叛乱，最后不得不废除封建。<br />
到了清代，进关之时，有过八旗兵的强圈民地，这里是皇帝一半默许、一半抢劫的半“皇封”的意味，但是，经过近300年的经济、社会变迁，所有这些土地，都变成了民间所有，不再有任何“皇封”的味道，就是说，这时所有的地主，都已经演化成“经济地主”。<br />
这些经济地主的产生，有许多种情形：在战乱之后，把荒地或无主的土地据为已有；有的是农民通过自己的努力，上升为地主；有的是官僚“退隐林下”，购置土地，成了地主，比如像四川的刘文彩；有的是在城市里经商，在乡下买了土地，比如山西的“乔家大院”；有的是经营有方，成了大地主，比如山东的“牟家大院”；还有就是一些乡绅的小康之家，但是，却没有一个是皇帝封赏的。<br />
在1911年的辛亥革命之后，皇帝也被扫地出宫了，中国难道还会有“封建”地主吗？<br />
<br />
在两千多年的时间里，这种农村的生产关系，其中包括地主-富农-中农-贫农-雇农的橄榄型的农村结构，基本上适合当时的生产力，如果不是这样，就不能解释，2000多年的灿烂的中华文明是怎样创造出来的。<br />
在多数的历史时间中，地主与农民间的关系是合作的，地主有求于农民，农民的求于地主，对于农村中的雇佣关系，与城市里雇主与雇工之间的关系相似，没有那么尖锐的矛盾，而且还有大量的中农和贫农存在。<br />
&nbsp; &nbsp;作为例证，在西方完成资本主义的革命之后，逐步敲开了与中国贸易的大门，在1840年之前，中国对资本主义国家的贸易是出超而不是入超的，这就说明，当时中国的经济实力超过新生的资本主义，说明中国的农村生产关系适合当时的生产力。<br />
后来中国的经济逐渐衰败，原因有两个：一个是，资产阶级革命在西方完成之后，清廷坚持其专制的政治体制，不能与时俱进，在政治体制上落后于西方；另一个是，在帝国主义的侵略下，节节败退，割地赔款，苛捐杂税，不断增加，严重破坏了中国的生产力，这种负担，都主要落在农民（也包括地主）身上。在辛亥革命之后，军阀混战，各据一方，对中国的经济、特别是农村经济造成更严重的破坏。<br />
把所有这些造成农民贫困的原因，都归之于地主对农民的剥削，是不符合实际情况的。<br />
<br />
在大跃进和文化大革命中，被中共宣传部门认定最典型的“封建恶霸地主”就是四川的刘文彩了，他被说成是十恶不赦的恶霸大地主，残酷地对待农民、私设水牢、喝人乳，不一而足。泥塑“收租院”的“艺术”作品，响遍世界。<br />
实际的刘文彩的情况并非如此。<br />
刘文彩在乡里原也默默无闻，只是在他到他的弟弟、四川军阀刘文辉军中当了财务总管之后，才逐渐发迹，他在此期间，除了公职之外还自己经商，发了财，不管他的财富是正当所得，还是不义之财，都确然不是他作为地主剥削农民所得。<br />
刘文辉被他的侄子刘湘打败之后，刘文彩衣锦还乡，购置了大约12000亩（另说为8000亩）土地，当上了地主。他在还乡之后，并没有为害乡里，而做了很多善事，比如，他自己出资，修了公路，（一说是从安仁镇修到成都，一说是在安仁镇修了一公里的路），还自己出资建了占地2000亩的“文彩中学”（现在叫安仁中学），是当时四川最好的中学，并且把这中学的管理权与财权交公，等等。他还善待佃农，交租之后要吃了饭才走，水牢等等纯系子虚乌有，编造出来的。<br />
据说，香港有人为中国地主设了一个“排行榜”，刘文彩排在第33位，他的情况很有代表性了。（以上资料取自网络“百度搜索”）<br />
<br />
在史无前例的文化大革命中，忆苦思甜是中共最常用的宣传手段，痛恨旧社会，热爱共产党。但是，从全国许多地方都传来如下的笑谈：农村忆苦思甜，总是请三代老贫农作报告，到了最后，往往是，那贫农说：早年给地方干活，地主还给饱饭吃，饭吃不饱就干不动活，到了六零年就不行了，那是真吃不饱，什么吃的都没有了，真饿呀，……。这时，工作人员赶快过来，说，老大爷你累了，休息休息吧，那老贫农还不服气，说，我不累，还能说，结果总是生拉硬拽把那老贫农拉下了台。<br />
<br />
中国的封建社会在哪里？<br />
说中国是半封建半殖民地的根据在哪里？<br />
<br />
平均地权，“耕者有其田”，固然是美好的，但是，这却是农民社会的乌托邦，它反映的是小农经济思想，在实际上也行不通：总会有贫富的差距。就算每个农民都有了同样数量的土地，过了不长的时间，贫富差距就一定会出现，这是不以人的意志为转移的。<br />
农业或农民问题的解决，根本还要靠“大农业”，不过，这个问题需要另文讨论。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;2010-1-7<br />
<br />
附：中国的皇权专制社会与<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; 欧洲的封建社会的区别（节录）<br />
<br />
马克思所了解的封建社会，主要是欧洲在资产阶级革命之前的社会形态，除了有国王或皇帝之外，欧洲的封建社会形态有以下几个主要的特点：1、没有一个主体民族，有几十个独立的民族国家分布在这个面积不大的土地上；2、有贵族和领主；3、有教会和僧侣，他们参与政治，常常成为重要的政治力量；4、有议会；5、有法院和法律；6、有相当数量的市民阶层；也就是后来的资产阶级；7、有农民和农奴；8、大小城市有不等的自治权；9、阶级地位壁垒森严；10、欧洲的各国的贸易相当发达，这种商业资本，后来发展成为可以左右政局的一种政治力量。11、在文化上，是普遍的个人主义，个人的尊严和自由高于一切。<br />
国王、领主、城邦中的市民构成欧洲社会三种主要的政治力量，而不是地主与农民构成社会的主要力量，农奴处在“贱民”的地位，是没有政治力量的。（中间略）<br />
<br />
马克思所面对的封建社会就是这个样子，这样的封建社会可以从其内部产生资本主义，产生资本主义的生产方式，产生资产阶级和资产阶级民主。但是，也只有欧洲的社会形态是这样的，占世界人口数量最多的“亚细亚”的社会形态就不是这样的。至少中国的情况就不是这样的。说从封建社会可以过渡到资本主义社会这个公式，符合欧洲的情况。<br />
马克思眼中的欧洲就是全世界。但是，欧洲并不是全世界。欧洲社会的发展规律并不必然就是全世界所有国家的发展规律。<br />
秦以后的中国，一直到清的社会形态，都与欧洲有原则性的区别。除了皇帝之外，构成欧洲封建社会的十一大要素，中国一项都没有。中国的农民，与欧洲的农民也有原则性的区别。<br />
中国典型的封建社会是周朝，其主要特点是分封诸侯，也就是说，在中国这片土地上，除了有一个最高统治地位的宗主国之外，还有许多受封的小国，这些小国的君主就是诸侯。<br />
若从生产关系来说，学者们认为殷商时代是奴隶社会，但是若从政治体制来说，殷商时代就开始分封诸侯，那么至少，它是有封建的奴隶社会，或有奴隶的封建社会。至周代，则不论从生产关系还是从政治体制上看，就都是封建社会了，因为那时，既没有了奴隶，也同时在分封诸侯。分封诸侯，始终是封建社会的最基本特征。按范文澜所说，有商代的“分封制”，又有“周初大封建”（《中国通史简编》修订本第一编，第120页、133页，人民出版社1964年第四版）。据称，商代有八百诸侯，到周以后，诸侯国大为减少，但是整个中国的政权变得稳定了。<br />
这些诸侯国的特征是，共同尊奉周天子，自己同样拥有对下属分封土地的权力，拥有对自己所辖土地与臣民的行政管辖的权力，相当于现在所说的自治，他们还可以拥有军队，还可以代天子征伐，叫“替天行道”。<br />
自秦以后，中国就基本上不再分封诸侯了。秦始皇“废封建、立郡县、建立中央集权制国家”。不封诸侯，自然就不再是封建国家了。<br />
秦之后的中国，除了有皇帝之外，欧洲社会的十一大特点，中国都与之相左：1、在中国这片广袤的土地上，只有一个主体民族，也就是汉族，占主导地位，在二千多年的时间内，基本是统一的；2、在欧洲称之为领主的，在中国就是诸侯，自秦之后，诸侯消失了，同时，也没有了世袭的贵族；3、中国有寺院和庙宇，有和尚和道士，但是，他们从来就不参预政治，不是一种政治力量；4、中国没有议会，没有皇权之外的政治机构；5、中国没有法院，虽然也有法律和大理司，但那完全是为皇帝服务的，皇帝可以任意破坏法律，不论是秦律还是唐律，还是大清律，都不能约束皇帝，皇帝想杀一个人，是不必经过审判的，皇帝想增税，也不必经过“大理司”；6、中国有城市，当然也有城市居民，却没有具备政治力量的市民阶层；7、中国有完全独立人身自由的农民，没有农奴，有非封建的地主，他们构成中国最主要的人口成分，中国的地主同欧洲拥有土地的贵族和领主完全是两回事；8、中国实行郡县制，各级地方政府的官吏统由朝廷统一派遣，没有任何城市可以自治；9、除了最初几代的皇族与外戚社会地位是明显的之外，没有明显的阶级壁垒。中国实行文官治理，任何人都可以通过科举考试出任各级官吏，没有等级制度；10、国内贸易与国际贸易在一些朝代亦相当发达，但是，自秦汉以来，中国一直就实行重农抑商的政策，商人从来就不是一个重要的政治力量。11、在文化上，是普遍的奴才主义，一个男人的崇高理想是作一个忠臣孝子，一个女人的崇高理想是作一个贞节烈女，没有个人尊严，对皇帝下跪和对七品县令下跪，都习以为常。充满了奴才相，他们充满了对皇权的崇拜，卑躬屈膝，为了捍卫自己的奴才地位而残害同类，并且引以为荣。<br />
（在西汉和西晋曾有过暂短的分封同姓王的时期，但是由于叛乱，由于七国之乱和八王之乱，很快就不再分封了。从秦之后，中国只要一“封建”，马上就叛乱，这种情况一直到明代。）<br />
<br />
经过这样的对比，我们可以看出，中国的所谓“封建”，与欧洲的封建，实有天壤之别，把适用于欧洲的一套硬搬到中国来，是何等的荒谬，硬说中国也可以自发地产生资本主义，岂非是梦呓。<br />
既然不能产生资本主义，自然也就不能产生资产阶级，不能产生资产阶级，自然也就不能产生资产阶级民主，这就是从社会阶级构成上说明，中国没有民主的根基。<br />
