																											<div id="postmessage_828570" class="t_msgfont">此文专为新近在阳光卫视播放的四十集抗战文献纪录片《国殇》而作，省载于9月9日的《亚洲周刊。<br />
<br />
一：我们是一群不肖子孙<br />
<br />
国民政府领导下的抗日战争，为世界反法西战争和中华民族的独立解放，做出了永垂青史的卓越贡献。六十五年前的硝烟已经散尽，战死疆场的健儿和无辜死难的民众，达三千五百多万，都墓木已拱，化为泥土，消息于天壤间。龚自珍诗云：落红不是无情物，化作春泥更护花。而这些饱受战争之苦、拚尽最后一滴血的健儿与民众，化作尘埃，融入泥土，依旧在滋润和保护着我们这些后代，但我们这些后代是否无愧于他们为民族独立解放所做出的牺牲和死难呢？答案是否定的。<br />
抗日战争是中华民族历史上最为惨烈的反抗外敌入侵的历史，但对这样艰苦卓绝的历史之真相，据有研究者估计，华人中却只有5%的人知道真相，而95%的人不知道真相，前者是包括台湾人在内的少数华人，后者则是包括大多数大陆人在内的华人。蒙元和满清入侵中原的历史，虽然过去了几百年，却保留下了许多难得的史料，有些细节至今仍历历在目。可是近在咫尺，只过去了六十五年的抗日战争为何95%的华人不知道真相呢？其原因在于中共官方的掩盖、歪曲、删汰，使得国民政府领导的为中华民族而战的抗日战争之真相被混淆，众多中国大陆民众所知的抗战历史，全是被中共官方所扭曲颠倒的历史。官方给大陆民众的说辞是，蒋介石所领导的国民政府不抗战，只有他们在抗战，是抗战的中流砥柱。直至2005年胡锦涛的讲话中虽然承认国民党领导了抗日战争的正面战场，却依旧说共产党是抗日战争的中流砥柱。<br />
像这样的谎言，稍微有点常识的人，便会起而质疑，亦不难揭穿，可是在中国大陆却还有许多民众因被愚弄太久而相信这样的谎言。一个偏安一隅，泥菩萨过河自身难保，且内部文件早已证实其为在抗战中自保壮大的共产党――关于共产党在抗战中如何发展壮大，可参看谢幼田的《中共壮大之谜》，四十二集电视文献片《一寸河山一寸血》里也有论述――怎么可能是领导抗战的中流砥柱呢？军事常识告诉我们，像日本这样集结上百万精锐之师的疯狂侵略，中国长大八年的浴血之战，怎么可能靠少量骚扰性的游击战取得胜利呢？既认同国民党在正面战场的贡献――国民党发动了22场大会战，1117场中型战投，38931起小型战斗，有多少是共产党参与的呢？为何共产党的将官只有左权一人牺牲，而国民党牺牲的将官却多达254位呢？――却又还说自己是中流砥柱，这种明显骗人而矛盾的话却依旧照说不误，其逻辑不自洽不攻自破。就连办了许多抗战博物馆群落的建川博物馆馆长樊建川，也只有按官方意志称中共抗战馆为中流砥柱馆，且面积远大于国民党的正面战场物馆，才能在大陆生存下去。<br />
《一寸河山一寸血》里展示了即令如美国这样对华援助不少的盟友，亦有许多让人不胜欷嘘之处。盟军的外交是现实的，重欧轻亚的政策，让疲惫羸弱不堪的中国政府独立支撑了很久抗击日本的任务，这是比较全面了解抗战历史的视角。但对从国共内战开始就妖魔化美国的大陆政府来说，其人民已经被政府意识形态束缚下的美国观所绑架，早已忘记了抗战时有许多美国人来中国抛头颅洒热血的历史。因此像建川博物馆的“美军援华馆”这样的抗战纪念馆之建立，是有尚有良知的举措，至少让美国人知道大陆人中还是有人懂得感恩，不全是忘恩负义的白眼狼。当建川博物馆的“中国壮士”广场落成时，有次樊建川邀请了一些共产党领导人的后代来做个活动（见凤凰卫视所拍《峥嵘岁月：成都建川博物馆的非常记忆》十集纪录片），他心怕这些共产党的后代不向包括蒋介石等国民党将领在内的“中国壮士”鞠躬，毛泽东的女儿李敏告诉他：都是我们的长辈，鞠个躬没有关系。李敏的特殊身份，使她不能不受意识形态的约束，但她用中国人的伦理常识来超越了意识形态之争，也算是作为后辈的一种聪明。<br />
同样，《一寸河山一寸血》里有蒋纬国两处谈到蒋介石时，令我记忆深刻。在抗日战争最艰苦的时刻，蒋介石疲惫一天，回家洗澡时都要用“大叫”来舒缓自己的紧张情绪。另一次是到台湾后，蒋介石视察一个装甲兵工厂，碰到一位老工人，很讶异他还在工作。蒋问他何时开始工作的，他说自己是从汉阳兵工厂就开始做工的，蒋劝慰勉励了他几句。后来蒋欲走，老工人追着说：委员长，你这几十年，太辛苦了，太委屈了，你要保重。蒋介石很受感动，回家吃晚饭时，他对蒋炜国说：老工人所劝慰鼓励我的话，除了你祖母外，没有第二个人跟我这样讲过。由此两点，可见出蒋炜国观察人事人性化的一面，也可见出蒋介石领导的政府对抗战尽心竭力之一斑。我们可以批评蒋介石及其政府诸多不是，可以抨击他的独裁和一些不当的政策，但我们却不可抹杀蒋介石政府领导中国人民在抗日战争中所取得的艰苦卓绝的伟大胜利。<br />
时至今日，有多少大陆人知道抗战时我们先人血染沙场、为国战死的真实历史，牺牲三千五百万将士和民众所带来的民族独立解放呢？我们不知真相，也没有想知道真相的欲望，更不知感恩祷念，只能说我们是一群行尸走肉般的不肖子孙，而这一切在很大程度拜大陆官方扭曲宣传、掩盖历史之赐。<br />
<br />
二：抗日将士被惩处和冷落<br />
<br />
当然大陆也不是没有人在努力打捞抗战的历史记忆，如四川民间收藏家樊建川和央视的崔永元都在做着一些努力――还有李肖伟编著的几大册美国陆军第二十航空队赴华作史料集如《超堡队：B―29 IN CHINA》等，惜乎印量太少――虽然他们依旧在不少地方无法突破大陆意识形态的束缚，但这种渐进式的挣开真相，还是值得赞赏的。特别是樊建川以一己之力，建成了中流砥柱馆、正面战场馆、川军抗战馆、援华美军馆、川军抗战馆、抗俘馆等，对打捞抗战真相倾尽了自己的心力。尤其是其中的正面战场馆、援华美军馆、战俘馆，堪称其中的用心之作。<br />
但这并不表明樊建川在大陆一帆风顺，且不说他个人遭遇的资金瓶颈和其它方面的压力，单是官方对他的“猜忌”，就足可以说明国军抗日将士六十五年来被打压和冷落之一斑。2005年国民党名誉主席连战给樊建川的“正面战场馆”题名为“国民党抗战纪念馆”后，就曾遭官方的打压，不准其宣传，但难能可贵的是他通过委曲求全的方式坚持了下来。特别令人感动的是，他单独为战俘所建的“抗俘馆”。“抗俘”是他生造的一个词汇，这个生造的词汇，我想代表了樊建川两层意思：一是抗战俘虏，二是反抗的俘虏，前者是实然是事实，后者是应然是价值判断，我想樊建川的侧重点在后者――有一层私人因素促成樊建川“抗俘”观的认识，即他的父亲就曾当过俘虏，这是他对自己父亲最好的体认和纪念――准此，我对樊建川所建的这个抗俘馆表示高度的敬意。因为中国人对被敌人俘虏的人，往往不抱同情之理解，没有对个体生命的尊重，只有单向度的杀身成仁，这样的战俘观是对人权的一种伤害。我们对那此杀身成仁的将士，固然要致以十分的敬意，但对于不幸被俘者，也要有感同身受的关怀。樊建川抗俘馆中有一张著名的照片，即安徽一位被俘的女战士成本华面对日寇即将的杀戮，表现出轻蔑不屑的笑意，实在令人震撼。像这样的俘虏不只是“战俘”，视其为不屈的勇士也不为过。<br />
共产党的抗战宣传，当然是以党派利益为最高鹄的，而非国家及民族的利益。大的且不表，我们从一个小细节来观察，便可证实我这个结论不诬。薛岳、孙立人、张灵甫、廖耀湘、王耀武、胡琏、杜聿明等众多抗日名将，在大陆是不折不扣的反面人物，甚至是十足的战犯。原因在于，国共内战时他们作为国军打过共产党，且不说兄弟阋于墙，共产党打国民党的正义何在？单拿共产党时常自诩的一分为二的辩证法来看，也不应该抹杀薛岳等人抗战的赫赫功绩。反之，像张自忠将军这样未及与中共打内战的殉国抗日将领，偶尔还会被大陆官方作为统战对象而被提及。而另外一些抗日名将则永远淹没在中共官方“中流砥柱”的宣传中，让大陆人民无从知晓。“中美合作所”诚然在国共内战时关押过共产党人，但其在抗战时，却为中国抗战的胜利立下了汗马功劳，但共产党却从来不曾一分为二地论述过这一事实，而是用党派利益掩盖其曾经为民族解放做出的贡献，不仅愧对美国人对中国抗战的帮助，更是厚污美国人在中日抗战中的牺牲。<br />
至于曾经的抗日将士之被惩处，在四九年后可谓不计其数。因为这些未曾死于日本刀枪之下的勇士，帮助过国民党打内战，哪怕被俘后也有不少遭武力消灭。甚至有的未曾参与过国共内战的人，只是曾经当个国民党的兵参加过抗战，也被视为反动人员（或反革命）而遭枪杀、监禁、改造，打入另册，五十年代初的“清匪反霸”中错杀，遭冤枉者不少。柏林自由大学教授郭廷钰在其《中国现代史》中指出四九年后在中国大陆，“国军被害者约三百万人，国民党遗留在大陆的党员被害者约一百四十万人”。再者，九十年代以降，被民间相对注意较多的远征军将士，有很多人在悲苦凄惨中生活，至今没有得到丝毫的救助和应有的尊重。不特此也，文革时期，不少抗战中牺牲的国军（包括地方军）将领如王铭章等人的坟地被毁，尸骨无存。腾冲的“国殇墓园”安放着3346位抗日阵亡将士的忠骨，此墓园被捣毁，不少将士的尸骨暴露于野。岳麓山附近的73军阵亡将士纪念牌，埋葬着参加三次长沙大会战死亡将士的忠骨，在文革时也被红卫兵捣毁，像这样的事情在全国各地可谓不胜枚举。如果中共官方把这些为民族解放而阵亡的将士当作烈士来宣讲的话，那么这些为民族之战而捐躯的将士忠骨不至于大规模地暴尸荒野。想起那些暴尸荒野的累累忠骨，无处安生，不觉像被万吨黑暗劈胸而来，我们是怎样的一群不肖子孙啊！<br />
<br />
三：抗战历史被查禁与歪曲<br />
在《一寸河山一寸血》的基础上演变而来的四十集《国殇》文献纪录片，本着挖掘抗战真相、为民族英魂和后人负责的态度精良制作而成。该片导演之一陈君天认为：“所有的数据都要正确无误，同时要‘第一手的当事人’，也就是亲临战场的将军与士兵：这些人都是历史的目击者，因为我坚持第一手的资料，不要‘听说’，儿子听爸爸说的我都不要。影片中引用专家的评述也很少，什么叫专家？只因为他多读了几本书嘛。书我可以自己去看……。所以如果他讲话还讲得清楚，在军官中有一定的层级，不管多远，我们都会去采访他……。”正是本着这一丝不苟的态度，使得阳光卫视即将播出的《国殇》一片，成为了解中日抗战历史真相的精品。<br />
由于大陆官方对抗战历史的有意歪曲，要恢复抗战历史的真实面貌，绝非易事，许多大家习以为常的称呼里面就暗含着对历史的遮蔽和歪曲。别的不说，今天有多少重庆人，知道曾作为战时陪都的重庆，在抗战胜利后所立的纪念牌叫什么名字呢？这座全国性质的抗战纪念碑叫“抗战胜利纪功碑”（由1940年抗战时期所立的“精神堡垒”演变而来），在1949年新朝得鼎后就更名为“人民解放纪念碑”（简称“解放碑”），成为大陆官方自夸“解放”人民和掩盖抗战真相的纪念碑。谢幼田《中共壮大之谜》一书关于中共不抗战真相，以及他们如何歪曲抗战历史，都做了不错的研究，像“解放碑”这样歪曲历史还算是小焉者也。但历史的真相不仅是由大事件的真实性构成的，而且是由小事件，以及日常称呼（如国共战争被大陆官方称为解放战争等）所组成的。这些日常性的称呼（如“解放碑”的篡改）对一个人潜移默化地历史记忆，有相当大的作用，研究历史真相和记忆者切不可等闲视之。<br />
为了漂洗自己不干净的历史，同时为了掩盖国民党领导下的抗日战争历史真相，五、六十代，大陆官方禁毁了许多书籍，其中就有不少描绘抗战历史事实的书籍被禁毁。因为众多四九年前所写抗战历史的书籍和连环画，对大陆官方宣传自己“领导”了抗战一说，非常不利，因此在众多书籍中将抗战历史真相的书籍焚毁，自是意料之中。禁毁书籍中，除了真正诲淫诲盗的作品外，大致可以分为三类，其一是揭露和批评共产党的；其二为如实（包括表扬）写国民党的；其三涉及抗战历史真相的。如1955年十一月四川省暨成都市处理反动淫秽荒诞书刊图书办公室编印的《处理反动、淫秽、荒诞图书目录（文字图书）》（上标注：内部文件 请勿外传）第一辑，就有丁韶光所著的三部书因“污蔑共产党，反共反人民”而被禁毁：《共产党的把戏》、《铁幕后的华北》、《揭穿共产党的黑幕》（出版社标为“伪国防部政工局”），其它因此条理由被禁毁书籍还有很多。再者，与抗战历史有关的小说如仇章的《遭遇了支那间谍网》等五种（均为正光书局出版），其理由均为宣传了国民党在隐形抗战战线上的情报战，《遭遇了支那间谍网》一书被禁的确良理由是“颂扬战犯李宗仁、白崇禧、陈诚、顾祝同等，歪曲抗战历史”。</div>

							
							
							
							
