													<h2>真・南京淘碟指南V08.08</h2>
						
						
																											<div id="postmessage_96476" class="t_msgfont">在南京的DY大致是全国最幸福地一批，除了低廉的价格，当然还有一个比较宽松的环境。林立的DW，齐全的品牌，还有各式的DY……<br />
<br />
今天就大致说一下比较不错的购碟去处：<br />
<br />
1，近来最有意思的事情，是原来的菲林馆拆分为两家，两家还在原址，靠在一起：我比较喜欢去分出来的星辉音像，品种之全之多恐怕这个世界都少有这样的碟屋。可以订购原版，YLJ还有大量的各种的港片。老板一直也是DY，所以对碟市品牌和货源都很有研究。基本上只要出过消息树的碟子他们家都会进到，不用望梅止渴！<br />
<br />
<br />
2，新菲林馆是原来的老板之一弄的，主流品种到货多而且快。老板爱结交朋友，比较幽默。虽然我现在不光顾，但是还是经常去转转，聊聊。<br />
<br />
<br />
3，离的最近的是小毛音像，门头是个照相馆，中山路上挂羊头卖狗肉。南京最早的一个DW之一，弄的艺术片系。老板自己多少也琢磨点东西，市面最新的艺术片包括CC啊，小津啊，老卡啊，基本都能在他家翻到。曾经比较欣赏的地方啊。可惜家有猛妻，老板人不错，太太实在难搞，常常以斤斤计较状待人。加上现在星辉基本满足了我的需要，所以长久不去了。<br />
<br />
4，PAGE2，据说经常有人开车去收。以前碟子的进货比较快，经常有独家最新到货之类宣传。时不时在过年过节也有一些优惠促销。其VIP的制度有点争议，不过我个人拿了一个VIP之后也很少去光顾了，兴许是跟他家的网络QS有点犯冲，总觉得P2有点先敬罗衣再敬人的姿态。不过我还是要推荐一下他家的服务，比较细致，所有留碟都单独摆放并且编号，不会出错。现在其改变策略，转为网络经营，不过一定要达到一定消费才能免费送货上门和邮寄……<br />
<br />
<br />
5，南京最老牌的还要算红帆，以前在金贸旁边中山电子城对面搬到了估衣廊。牌子老意味着资格老脸皮厚，价格死高地。现在去红帆买碟如果不是掏老货就意味着脑子有病了。<br />
<br />
6，其实如果要抢快，还有一个去处。夫子庙的兰色部落，基本就是一个简九的堆场，前面有什么货漏掉，基本可以在这里补到。价格一般，不怎么还价。另外最特色的是各式假品牌碟和快板。<br />
<br />
7，望升州路这里走，小巷子里一家音画时尚，只去过一次，老货为主，老板比较懂碟子，但是可能是小本经营关系，店面一般，现在据说搬家也不知所踪。<br />
<br />
8，城西头凤凰西借莫愁新寓一带有几个正版简装的地不错，经常可以拿到简单修复的ZK港片。价格不低。<br />
<br />
9，真正喜欢拿这类地最好去板仓，一个批发市场。不过如果你买的数量不大还是高价给你的。最好跟几个朋友一起去，那里的港片经典真叫个多啊。<br />
<br />
<br />
10，好象跟媒体朋友关系最好的还是秋明和四海，经常在报纸上看到小编报告在秋明发现了什么什么……好象看到新大陆一样。我对秋明和四海的感情比较深，深在2002-2003年刚刚全身心投入DVD那阵子。除了可以在那里发掘打口CD，还在那里买到我人生第一套COWBOY BEBOP。记忆尤新。不过随着时间，渐渐少去了。不过有老外朋友，我还是推荐离他们学校比较近的这两家。<br />
<br />
<br />
11，最后不得不说下马台街小魏，是南京D版圈的一块招牌了，很有意思的是，LB据说抛开生意不管开茶馆了。以前做过几次活动，幅度都很实在。是唯一感觉到在活动中受惠的一次。<br />
<br />
其他一些如三牌楼或者市井小店就不介绍了。鼓楼星星的名气很大，但是老板被很多DY众口铄金，至今只做了一次他的生意。<br />
<br />
南京的买碟去处也大概就这么样。如果有需要知道详细的请直接PM我。谢谢您花时间看了。</div>

							
							
							
							
