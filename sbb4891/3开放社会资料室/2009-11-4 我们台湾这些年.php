1980&nbsp;&nbsp;“美丽岛大审”改变了陈水扁的一生 <br />
<br />
进入20世纪80年代，一切似乎都开始焕然一新，国民党在台湾的严密体制也开始出现了些微的松动，一切都要从这年的美丽岛事件大审开始。 <br />
《美丽岛》杂志的核心人物被一网打尽，当时党外参与者皆有坐牢的心理准备，所以这次运动的悲剧色彩颇为浓烈。这个“政团”可说是瓦解了。经过审判后，核心八人通通被判“叛乱”，分别是黄信介、施明德、张俊宏、姚嘉文、林义雄、陈菊、吕秀莲、林弘宣。被判有罪是可预期的，但其影响及后坐力是相当大的。 <br />
这次审判不仅引起全台湾民众的关心，国外的人权团体及媒体也很关心。国民党为了展示开明作风，虽然这是一次军事法庭审判，但也没像以往做太多的限制与操纵，因此各大报皆可找到判决及答辩全文。虽然法庭上摆满了袭警的“证物”，但这些答辩，已经不仅仅在于“是否有袭警”的层面，而上升到了对台湾前途的关心、定位的政治层面。法庭似乎变成了他们的政见会场，一个个轮流侃侃而谈。 <br />
有一个关于“美丽岛大审”的笑话是这样的：当年美丽岛军法大审时吕秀莲有一条罪名是说了“打拼”两个字，这两个字在闽南语里是努力、奋斗的意思，但当时的军事法官不懂闽南语――“又打又拼，这不是叛乱是什么？”于是又罪加一等了。 <br />
因为信息如此透明，这些言论也为台湾民众新开了一扇窗，上了一次值得好好思考的政治教育课。的确，这些言论如解除军事戒严、“国会”全面改选等，虽然在当初被视为洪水猛兽，倡议者也被冠以“叛乱分子”、“野心分子”等恶名，但这些诉求在30年后的今天看来都已经是理所当然的权利。 <br />
<br />
在“美丽岛军事大审”的同时，发生了一件震惊全台湾的灭门血案――林宅血案，林即指当时正在接受大审的主嫌之一林义雄。在2月28日这天，他的母亲在自宅被人刺了13刀，七岁的双胞胎幼女各被刺一刀丧命，而长女被刺六刀重伤，后经急救脱险，林义雄的妻子则因外出而幸免于难，此案震惊海内外。凶手手法极为专业，以短刺刀捅入，接着横向反钩，刀刀置人于死地。 <br />
因为时间正好在大审的敏感期间，又因刚好是2月28日，因此很难不让人往政治谋杀的方向联想。当然，就算真的是国民党干的，他们也不会承认，因此警方设定为“国际阴谋集团”的罪行。但当时不少人认为，林宅血案是国民党当局在美丽岛事件后，为了压制日益崛起的党外反对运动，所采取的杀鸡儆猴的血腥示警手段，旨在警告所有台湾人，挑战国民党权威者，必将在台湾重演“二二八”事件。当然，我们现在看来这样的联想缺乏证据又太过主观，但在那个时代的背景下，这个理由似乎又成为最合理的解释，警方当初侦办的方向反倒显得荒谬不堪。 <br />
后来林义雄还在狱里，林家生活困难想把房子卖掉，但没人敢买，直到长老教会将它买了下来作为教会来使用。每年的2月28日，除了纪念1947年的“二二八”事件外，大家免不了想到的就是林宅血案。每年在这里也办纪念的音乐会，总是上新闻。但林宅血案终究成了悬案，到现在还没破案。每次一说到这件事，年纪稍长的人总是变得很沉重。在台湾，不分政治立场，对林义雄总是抱着尊敬的态度，称之为“人格者”（闽南语，人格崇高者），总觉得台湾亏欠林义雄很多。 <br />
<br />
在另一方面，这次美丽岛事件审判的辩护律师团里，也都是一群年轻且不畏强权压力的热血律师。虽然这是一次可预见的审判，但他们经过不断努力，从幕后走到台前，在这次审判后纷纷投入党外运动，成为后来反对运动的中坚，甚至风云际会成为政治场上的要角，如陈水扁、谢长廷、苏贞昌、张俊雄等人，都是当时的辩护律师。当时谁也没想到，这次的辩护，几乎改变了他们的一生。 <br />
而在国民党一方，当时负责给政府化装的“新闻局长”就是宋楚瑜，他在那时是国民党栽培的明日之星，他也很尽责地做好一个发言人的角色。而后来几十年里，两方中坚分子的不断交锋，恩恩怨怨纠缠了几十年，双方人马在政坛皆起起落落，分分合合，故事好不精彩，留着慢慢说。 <br />
现在台湾蓝、绿在那边吵吵闹闹的，但很少有人会否认美丽岛事件是台湾加速开放的一个重大转折点，具有分水岭的意义。台湾经济发展牵动的参政意识，以及要求资讯的流通，均使政治多元化成为不可避免的趋势。正是以前那些人敢于争取，现在台湾才有吵吵闹闹的机会。<br />
