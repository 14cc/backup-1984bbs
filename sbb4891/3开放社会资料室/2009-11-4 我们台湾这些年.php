1979 我的家庭 <br />
<br />
&nbsp; &nbsp; 1979年对于台湾来说，实在是不怎么安宁的一年。1月1日，美国与台湾当局断交，年底又来了个“美丽岛事件”，我看当局头都大了。 <br />
&nbsp; &nbsp; 但对一般百姓来说，1979年最大的事就是开放去岛外观光。在过去，只有经商、公务、留学等才能离开台湾，现在没有具体的事情，也可以离开台湾观光了，因此，香港瞬间就被台湾人“占领”。 <br />
&nbsp; &nbsp; 而我家的大事就是，我妹在夏天出生了。在20世纪70年代末的台湾，当局鼓励的生育政策是“一个孩子不算少，两个孩子恰恰好”这种大家都会背的口号；另外，那几年还有一种口号叫“三三三一”，指婚后三年生第一个小孩，过三年再生一个，理想的结婚年龄为男28岁、女25岁。我爸妈还真响应号召，刚好生了一男一女，一家四口，标准地迈入中产阶级家庭。 <br />
&nbsp; &nbsp; 在这之前，台湾人总是喜欢多生，像我父亲就有七个兄弟姐妹，母亲家也有五个。越穷的人家越喜欢生得多，看起来好像不太合理，但若从经济学角度来看，过去台湾比较穷，一般百姓必须多生增加机会，这是可以理解的。但到20世纪70年代就开始有了人口压力，当局开始鼓励少生。这几年口号又改啦！“两个孩子恰恰好，女孩男孩一样好”，特地将女孩放在男孩之前，希望消除重男轻女观念。后来因为少子化的风潮，又改成“两个孩子很幸福，三个孩子更热闹”，希望民众多生几个小孩。 <br />
&nbsp; &nbsp; 总之，我多了个妹妹，只差我一岁而已。由于年龄相仿，所以我跟她不存在什么“哥哥保护妹妹”这种事，反而从小打架打到大：她咬我手臂我抓她头发，吵吵闹闹的，好不热闹。当然这样也建立了深厚的感情。 <br />
我家在这一年也留下了第一张全家福：妈妈抱着妹妹，爸爸挽着妈妈，我骑在小木马上。 <br />
<br />
<br />
&nbsp; &nbsp; 台湾当局与美国“断交” <br />
<br />
&nbsp; &nbsp; 1979年1月1日零时起，台湾当局与美国“断交”。或者从大陆的角度来说，是中美建交。 <br />
&nbsp; &nbsp; 当时的台湾当局其实就是陷在这种迷思当中，以中国的正统自居。在蒋中正的时代，一直以极端的原则处理两岸问题，无形中也使台湾的国际空间缩小了很多。 <br />
&nbsp; &nbsp; 总而言之，“断交”已成定局，民间再多的抗议和自发的政治运动，在大国博弈间也显得软弱无力。台湾少了个靠山，信心不足的人就纷纷移民，在那一年就有大规模的移民潮出现。于是，才有了蒋经国在那年的除夕谈话“渡过难关”的言论出现。 <br />
&nbsp; &nbsp; 当时任“新闻局长”的宋楚瑜，在记者会上表达台湾对“断交事件”的“严正立场”，他凭借英挺的形象，可谓一夕间爆红，成为媒体宠儿。就在那个台湾人心极大动摇的时刻，知名的民歌手侯德健也发表了《龙的传人》这首歌，整首歌词歌颂古老神州的庄严伟大、近代中国的苦难以及对故土的眷恋之情。当然啦，这首歌呼应当局的立场，在当局的大力支持下，立刻在台湾及海内外华人间歌颂一时。 <br />
&nbsp; &nbsp; 而在社会上，也掀起一波又一波的“爱台湾”运动。比如，小的时候看过一个故事，说一个小朋友在美国与台湾“断交”后，同仇敌忾，捐出自己的猪公扑满①来给当局买“飞机大炮”。我大一点儿的时候就想，飞机大炮不还是都跟美国买的吗？这个故事好像怪怪的。回到正题，台湾当局与美国“断交”后，美军跟着撤离台湾，取而代之的是“与台湾关系法”。美国政府根据“与台湾关系法”，继续供应武器给台湾。台湾继续以“自由的灯塔”自居，一方面也是安抚民众，但明眼人都看得出来，所谓的“反攻大陆”已经成为笑话，只有当局继续喊得响，底下的响应一年比一年心虚，一年比一年微弱。 <br />
&nbsp; &nbsp; 当然，美军在台时带来的一些经济活动也随之萧条，许多社会问题渐渐浮现，尤其是美军留在台湾的私生子问题，成为20世纪80年代许多电影及文学作品探讨的对象。 <br />
&nbsp; &nbsp; 此外，侯德健后来在1983年潜赴大陆，台湾当局感到脸上无光，遂把《龙的传人》这首歌给禁掉了，当时还成为许多人揶揄的事件。 <br />
<br />
