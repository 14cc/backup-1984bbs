1980 台湾当年的电视台 <br />
<br />
&nbsp; &nbsp; 美丽岛事件以及来年的大审，绝对是影响台湾民主化进程的一件大事，不管是当初给台湾人民的震撼力，还是后来对当事人、辩护律师们的未来发展，都影响重大。 <br />
&nbsp; &nbsp; 我当年还小，没什么感觉，很多事都是后来从长辈口中听来的。那个年代小道消息特别多。当然啦，当局控制着几个不自由、无法满足人民知情欲望的媒体，社会上小道消息或谣言自然传得就快。在开放电视频道以前，台湾的无线电视台只有三个： <br />
&nbsp; &nbsp; 台湾电视台，简称台视，是台湾省政府所有的，也是台湾第一个电视台，所以常常会看到一些农渔民情报道，那时普遍认为台视的报道会稍微客观一点儿。 <br />
&nbsp; &nbsp; “中国电视台”，简称中视，是国民党所有，所以很多都是国民党活动内容，内容嘛，在以前也比较“忠党爱国”一点儿。 <br />
&nbsp; &nbsp; 中华电视台，简称华视，是军方所有，所以每周四都会有所谓的“莒光园地”，部队里的阿兵哥每周四都必看这个节目，接受一下洗脑，不过大多数人都是在补眠，一直到美女主持人又出现为止。 <br />
&nbsp; &nbsp; 在20世纪70年代，台湾电视收视率最高，也是史上收视率最高的节目，是闽南语布袋戏“云州大儒侠史艳文”。布袋戏是一种人偶戏，当时在中午时段播出。一到中午，街道空无一人，收视率达到百分之九十几。后来因为实在太猛太轰动，据说当局以“妨碍工商活动”之名给禁播了。 <br />
&nbsp; &nbsp; 话说回来，虽然当时台湾有这三个电视台，但在那个“党领导一切”的时代，报道的内容都是审核过的，大同小异。不过这三家电视台也保持着既竞争又合作的关系，比如当局重大庆典时就来个联播，三台各派出当家主播和主持人负责一个部分。而三台节目又各有特长，比如说台视长于新闻，现在很多有线新闻台的主管最早都是台视出来的。中视长于戏剧，台湾第一部连续剧《晶晶》就是中视推出的，许多大陆朋友熟悉的琼瑶剧，如《梅花三弄》、《还珠格格》也都是中视播出的。不过这几年很多剧都外包给大陆，或为了节省成本，索性直接买大陆剧来播，算弱掉了。华视虽然是军方所有，但长于综艺节目，从前到现在一直如此，许多名主持如张小燕、胡瓜等人，也都是在华视发迹的。 <br />
&nbsp; &nbsp; 离题了，下面来说美丽岛事件。 <br />
<br />
美丽岛事件 <br />
<br />
&nbsp; &nbsp; 在民进党成立以前，台湾并没有严格意义上的反对党。除了国民党外，名义上还有青年党、民社党等，每次选举他们也都会派候选人出来，但根本威胁不了国民党，或者说，这些党只是国民党包养着的，有点儿用来宣传“看！我们还是有其他政党！”的意义而存在。所以，台湾当时基本上还是国民党以党领政、国民党压倒一切的情况。 <br />
&nbsp; &nbsp; 当时有个词：党外。国民党一党独大的统治时期，台湾社会没有组党的自由。在早期反威权和自由主义的组织或个人，是以杂志等文宣来表达宣传自己的政治主张。到了20世纪70年代，开始通过选举的机制，进行进一步的串联和组织工作。 <br />
&nbsp; &nbsp; 这里不得不提到一个词：白色恐怖。不解释词义，请大家自己去查。这个词对于台湾的意义，大部分就是指国民党自统治台湾以来对异议分子或嫌疑者的肃清及迫害。台湾人民长期处在这种环境里，有话不敢说，有意见不敢表达。表面上，法律赋予大家有集会结社自由，但解释权完全操纵在国民党手上。 <br />
&nbsp; &nbsp; 20世纪70年代以前，有所谓“动员戡乱”体制，“国会”完全不让台湾人民改选，直到1969年之后，经修改“动员戡乱时期临时条款”，也只改选“国会”中的一小部分名额而已。当时，“国会”成员几乎都是一些1949年跟着来台，代表台湾各省的议员。但几十年过去了，这些人也慢慢开始老死，终于有了替补的空缺，党外异议人士终于有进入体制内发言的机会。但是国民党简直就是球员兼裁判，规则怎么玩都是他们定的，一般人怎么能玩得过他们呢！所以，国民党跟党外的关系也越来越紧张。 <br />
&nbsp; &nbsp; 这时的反对运动跟更早期的不太一样，不是松散的书生论政，这些人也不是无党籍的地方士绅，而是一群“行动者”，懂得结合政治及社会思想去做“运动”。另外，“反攻大陆”让人感觉越来越不现实，所以，官方的意识形态及1949年后国民党带来的外省政治精英的统治正当性及政治权利分配性，也开始不断被挑战。因此，台湾本地的政治精英人物开始成为主要的反对力量。 <br />
&nbsp; &nbsp; 1979年，一本叫《美丽岛》的杂志出版了，这是由一群来自各地党外人士所创办的，一时精英聚集，大有政团之势。在一言堂的时代出版了这样一本异议杂志，大家看得爽，心里出了一口气，自然很喜欢。《美丽岛》的模式是到处设分社，每设一处分社就在当地办演讲，大受民众欢迎，但不容见于官方。 <br />
&nbsp; &nbsp; 《美丽岛》高雄分社在当年12月10号国际人权日举办游行活动，不料却引发严重的警民大暴走，冲突规模之大，可说是台湾史上首见。 <br />
&nbsp; &nbsp; 直到现在，发生冲突的原因还是众说纷纭，有很多人说是当局派人在民众里当托，故意攻击警察，但到现在为止，还是没人敢证实。 <br />
&nbsp; &nbsp; 在这次事件中，活跃于党外阵营的主要人物几乎全数被逮捕，当时台湾的反对运动受到严重打击。很多人说，蒋经国被这件事震撼到了，所以加快了开放的脚步。但不管怎么说，这件事的结果，以及后来的审判，的确让很多政治冷感的台湾人开始思考一些政治及台湾前途的问题，成为一个启蒙的起点。<br />
