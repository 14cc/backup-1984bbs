google文档（收集了部分）：<a href="http://docs.google.com/Doc?docid=0ASX_9pSQEBo9ZGZzOXo0NWhfMGNianF0ZGMy&amp;hl=en" target="_blank">http://docs.google.com/Doc?docid ... ianF0ZGMy&amp;hl=en</a><br />
<br />
《我们台湾这些年》――讲述台湾老百姓自己的故事（1） <br />
　　 <br />
　　1977&nbsp;&nbsp;出生在被赶出联合国后风雨飘摇的台湾 <br />
&nbsp; &nbsp; 我是1977年生人。20世纪70年代末的台湾，整个社会气氛都是怪怪的，就像是一只气球，随时都可能破掉，但就是破不掉的状态。重大基础建设都快要完成，经济开始腾飞，但大家都在等那种“冲破”的气氛。 <br />
&nbsp; &nbsp; 这段时期的台湾，用后来官方一些政令倡导的文章来形容就是“风雨飘摇”。在台湾当局代表被赶出联合国后不久，接着就是1978年底的美国与台湾当局断交。人心惶惶的社会气氛下，有办法的人当然都先跑到别的国家去了。而台湾当局不断地告诉大家要“庄敬自强，处变不惊”――应该说，在更早的时候，从1949年国民党退踞台湾到20世纪70年代的这几十年里，岛内反对人士始终站在国民党对立面，却如狗吠火车般无用，甚至给自己带来危险；到了20世纪70年代，虽然内外交加之下岛内形势风起云涌，要求改革的呼声也越来越高，在那个“党即一切”的年代，社会上反国民党的声浪也从小涟漪成为小水波，越来越大，但国民党仍然坚持一贯的统治态度，因此这几年风波不断，草民如我家，也不由自主地被卷入某些事件里面。 <br />
我的生日在11月份，而我父母的结婚纪念日是在当年的3月29日。我年纪大一点儿时发现其中似乎怪怪的，因此动不动就爱跟我妈开玩笑：“乖乖，原来我是早产儿啊……”我妈那种保守的女人当然每次都被我逗得无话可说。 <br />
&nbsp; &nbsp; 简单说一下我爸。他是台湾中部云林县人，云林在传统上（甚至现在）和大家的心目中都算是乡下,传统的农业县。但也因为是较穷困的地方，所以民风也较彪悍，大家一提到云林，就不由得联想到黑道的故乡。而我妈是台南人，台南算是台湾最早开发的地区，因此文化气氛浓厚，传统上大家也认为台南人很有钱。 <br />
&nbsp; &nbsp; 我爸就跟他那个年代的大多数人一样，年轻时就到台北打拼，他本来在药厂做销售员，后来一直做到台湾北区的销售主管。在台湾高速公路刚建成，没什么私家车在跑，又被讥讽为养蚊场的年代，他就已经买了车子，算是那个年代很普遍的、从乡下来到台北，努力走向成功的商人典型。而我妈，年轻时在台南就自己开了一个打字行，刚好在我爸的台南总公司旁边，两个人就这样认识了。 <br />
&nbsp; &nbsp; 在他们结婚,即我出生的前一年，也就是1976年，我爸已经在台北做起了自己的生意，并住在台北士林的旧家，这是我3岁前住的地方。 <br />
<br />
&nbsp; &nbsp; 这年年底发生了一件震惊全台的“王幸男邮包爆炸案”，对象是当时的台湾省“主席”谢东闵，邮包炸伤了他的左手手指。这件震撼国民党党政高层的邮包爆炸案，据我高中时的老师回忆，在那指纹还无法用计算机辨识的年代，几乎所有大学生都被动员去清查核对，也就是逐一清查核对全台湾已服役男子的指纹资料。就这样，几十万份资料一份一份地查，居然也真的查出来了，原来是一位叫王幸男的人所为。他因不满政治情势，而做出了这样的举动。偏巧王幸男家的药厂，就是我爸工作的药厂。 <br />
&nbsp; &nbsp; 据我老爸回忆，他某天回家，看到有两个人坐在家里的椅子上。弄清他们身份后，我老爸就被带进某个地方喝茶，接着就是兄弟有几人、有没有女朋友、在教会干吗（我爸那时候已经是基督教徒了）、药厂都在做什么之类铺天盖地的问题。好几个人轮流问了24小时，最后我老爸写了保证书后才让出来。事后他才知道，那时候还是他女朋友的我妈，以及他的兄弟姐妹，也全都被问过话了。 <br />
&nbsp; &nbsp; 我爸对国民党的反感，可能就是从那时候开始的。 <br />
&nbsp; &nbsp; 不过话说回来，我爸可还是国民党党员呢！现在都还可以看到他那破烂不堪的国民党证，不过他每次都说他从没参加过小组活动，没交过一次党费，说是都有人帮他代签代交了。 <br />
&nbsp; &nbsp; 不夸张地讲，在那个年代，几乎大家都是国民党党员。只要上高中，学校教官或老师就会用各种方法、手段，明示、暗示学生要入国民党。当然，对他们来说也是有业绩压力的啦！高中生哪会分辨太多，只知道入国民党有许多好处，就乖乖地填了资料入党。或者在当兵时，部队辅导长也会要求大家入党。在台湾的义务兵体制下，尤其像几十年前那种一当兵就要当两三年的苦日子里，做国民党员在部队里可能会有比较多的好事爽差，大家就这样入了党。 <br />
&nbsp; &nbsp; 所以，现在在台湾问四五十岁的男人，谁没入过国民党，可能很难找到，只不过极大部分都没交过党费，没参加过活动，成了“失联党员”。前不久，国民党进行党员资料整理，还意外发现许多民进党及亲绿人士都曾经是国民党党员。 <br />
<br />
1978&nbsp;&nbsp;蒋经国继任与“十大建设” <br />
<br />
&nbsp; &nbsp; 1978年，蒋经国就任了。 <br />
&nbsp; &nbsp; 台湾人熟悉的那个画面――每年蒋中正穿着军装，在“双十节”阅兵时那挺拔的身姿和强有力的口音“军民同胞们……”变成蒋经国那亲切的挥手以及谦虚的致意“亲爱的父老、兄弟、姐妹们……”不变的是那浙江口音，但敏感的人似乎从这句话里听到了些弦外之音――时代开始改变了。 <br />
&nbsp; &nbsp; 蒋中正“崩逝”（是的，当时的报纸用的就是这个充满了皇权帝制意味的封建字眼）后，“副总统”严家淦继任，但实权却在“行政院长”蒋经国手上。严家淦可说是虚位“首脑”，当时都叫他“YESMAN”。虽然两年后蒋经国的接班已经是可以预见的事，但还是有不少人不太满意，不知道蒋家王朝还有多久。当初有个政治笑话是这么说的：蒋介石去世后，不可避免地在天堂遇见了“国父”孙中山先生，壮志未酬身先死的孙中山非常关心“中华民国”的状况，于是问老蒋―― <br />
<br />
&nbsp;&nbsp;“我死后，‘中华民国’有没有‘行宪啊’？” <br />
&nbsp;&nbsp;蒋介石马上回答：“有啊！有‘行宪’，有‘行宪’啦！” <br />
&nbsp;&nbsp;孙中山又问：“那第一任‘总统’是谁？” <br />
&nbsp;&nbsp;蒋介石回答：“是我。” <br />
&nbsp;&nbsp;孙中山心想，老蒋一统江湖，确实当得，又问：“那第二任呢？” <br />
&nbsp;&nbsp;这时老蒋不太好意思说还是自己，可又不太想说谎对不起“国父”，于是回答：“于右任①（余又任）。” <br />
&nbsp;&nbsp;孙中山高兴地说：“不错不错，书法家当‘总统’，文学治国。那第三任又是谁呢？” <br />
&nbsp;&nbsp;蒋中正脑筋一转，机智地答道：“吴三连②（吾三连）。” <br />
&nbsp;&nbsp;孙：“嗯，舆论界有人出任‘总统’，也好。那下一任又是谁？” <br />
&nbsp;&nbsp;蒋：“赵元任③（照原任）。” <br />
&nbsp;&nbsp;孙想了一想说道：“很好，语言学家当‘总统’。那第五任呢？” <br />
&nbsp;&nbsp;蒋：“是……是赵丽莲④（照例连）。” <br />
&nbsp;&nbsp;孙中山开心地说：“太好了，连教育家也做‘总统’了，真是越来越进步了。” <br />
<br />
&nbsp; &nbsp; 这是个很典型的讽刺蒋家的笑话。其实，蒋中正的影响力一直到20世纪80年代末90年代初都还存在，在我小学时都还要背“蒋公遗嘱”，音乐课也要学唱“蒋公纪念歌”。而每次老师一提到蒋公或“国父”孙中山先生时，大家都要正坐或立正一下表示尊敬。这些事现在回想起来，都觉得挺傻的。 <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp; 在此前一年，中山高速公路也通车了，这是一条纵贯台湾南北四百多公里的高速公路，也是台湾第一条高速公路。 <br />
&nbsp; &nbsp; 当初建成时，许多人批评这根本是一条超大型的养蚊场，因为根本没多少车子在上面跑，是富人专用的。但事后几十年证明，这条高速公路对台湾经济产生了重大影响，让商品货畅其流地在台湾西部三大港间流通。甚至到了后来，台湾私家车变多后，这条高速公路根本不敷使用。许多我这一代台湾人的成长经历里，一定都有过年爸爸开车载着全家走高速公路回中南部老家的印象，并且，一定会被堵在路上好几个小时。本来南北往来只要四个多小时，这时变成近十个小时或更久，整条高速公路变成一个大停车场。在晚上看来，车尾灯就如同一条红红的龙，盘到地平线的另一边。 <br />
&nbsp; &nbsp; 因此，后来当局又建了好几条方向重复的高速公路，以减轻车流负担。但高速公路在台湾除了交通功能外，还有很重要的战备功能，在某些路段上，是特别设计来供战斗机起降的。 <br />
&nbsp; &nbsp; 那几年，“十大建设”陆续完工。“十大建设”是指台湾在20世纪70年代时所进行的一系列基础建设工程。在这之前的台湾许多公共基础建设，如道路、港埠、机场、发电厂等，仍处于欠缺、老旧的状态。再加上1973年10月第一次石油危机爆发，受到全球经济不景气的影响，为了提升和深化总体经济发展，当局开始规划进行十个大型建设工程。这分别是：核能发电厂，中正国际机场，铁路电气化，台中港，中山高速公路，大炼钢厂，大造船厂，石油化学工业，苏澳港，北回铁路。在“十大建设”中，有六项是交通运输建设，三项是重工业建设，一项为能源项目建设。 <br />
&nbsp; &nbsp; 但当初任“行政院长”的蒋经国决定“十大建设”的计划时，是冒了很大风险的。当时台湾经济尚处于起飞初期，民众经济所得尚未提高，以及当时遇上第一次石油危机和台湾当局退出联合国等外交困境，可以说，台湾在发展“十大建设”时，当局是没有经费的。所以，有部分官员表示反对，但蒋经国以一句“今天不做，明天就会后悔”力排众议，后来通过找沙特借款才得以解决。也就是说，“十大基础建设”是举债建设。后来也证明蒋经国的决策是完全正确的，确实推动了台湾经济起飞。可以说，台湾经济完全起飞是在“十大建设”之后。 <br />
&nbsp; &nbsp; 以当时蒋经国的观念来说，他认为，在经济成长的过程中，“富可敌国”与“贫无立锥”如果形成两个极端，却被理解为经济真的成长了，这应是一大讽刺。所以，为了避免像许多高度开发国家那样财富集中，贫富悬殊，他认为发展不仅要财富增加，也要合理分配，这样人民才能在均富的标准下愉快又勤劳地工作，社会才能稳定。可以看到，“十大建设”里有七项是经济基础建设，对台湾未来来说，不管采取哪种发展策略都是非常必要的建设。 <br />
&nbsp; &nbsp; “十大建设”所带来的经济方面影响，的确给台湾带来焕然一新的生活风貌，但另一方面也牵动了政治与社会的演化。在人民知识与经济能力大幅提升的情况下，要求政治开放的呼声也越来越高，原来严密的政治控制体系已逐渐不能应对人民的要求了。 <br />
