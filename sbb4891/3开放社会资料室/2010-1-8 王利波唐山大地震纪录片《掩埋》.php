去年有关地震的媒体报道，众所周知有三个禁忌：地震预报、校舍垮塌、救灾款去向。然而，在这样一场世纪大灾难面前，搞纪录片的岂能沉默。就跨越禁忌而言，最早问世的作品有潘建林《谁杀死了我们的孩子》，该片去年9月在釜山电影节公映，向世人呈现了青川县木鱼中学校难的情景。2009年第六届中国纪录片国际选片会上，有“中央电视台记者编导”的《127张遗像》，作品直接表明：酿造悲剧的元凶不是地震，就是危楼。《南方都市报》在报导这条消息时未说明编导名字，但题材显然是关于绵竹市富新二小的故事。而我要推荐的是王利波有关唐山大地震的作品《掩埋》，该片获得2009年中国纪录片交流周优秀纪录片奖。在目前露脸的地震题材纪录片中，它是第一部探寻地震预报问题的作品。从网上查到：王利波，1973年生于哈尔滨，1999年开始影像创作，作品有多媒体戏剧《荒诞的墙》。2004开始纪录片创作。2007年参加了广州纪录片大会与宋庄艺术节《07影像档案展》，现居北京。在导演阐述中，王利波说：<br />
<br />
1976的唐山大地震留给世人的疑问太多，地震前唐山的地震工作者和北京的地震专家都曾发出过临震警告！但最终却以超过24万人的生命被掩埋为代价，造成了震惊中外的大悲剧。这到底是为什么？2008年汶川大地震约10万生命被掩埋。面对惨绝人寰的大地震，人类一次又一次的重复着悲剧的发生。人们仅仅是把金钱和无味的泪水献给不幸者，这是人类的悲哀。一个民族必须有勇气正视本民族的弱点，这个民族才能有希望。<br />
<br />
在王利波之前，全面揭示唐山地震漏报这个黑匣子的是河北作家张庆洲。经过8年采访，张庆洲在2005年5月出版了报告文学《唐山警示录》。他在这本书中，记录了当年参与地震预测预报各个层面知情人的回忆，呈现了地震界有关预报的争议，解开了唐山预报真相被隐瞒的谜团。王利波从这一著作获得采访线索，并得到张庆洲先生的支持指点，从而用影像的方式，让一系列著名的专家证人、还有民间知情人直接出场，面对观众讲述他们的观点。如果说，当前很多独立纪录片走的是叙事型影片的路子（例如记录小人物的生存状态），这部作品则堪称议论型影片。它聚焦于灾难性的社会事件和悬而未决的科学问题，按照唐山地震发生前后的时间线索，组织了当事人的思想观点。和报告文学不一样的是，通过影像，我们能够直接面对这些可能对政府决策、对24万亡灵和几十万伤残者的命运产生重大影响的专家、民间预测者；他们的言说，因而带来不同的视觉震撼。为历史留影、让证人发言，这本是历史题材纪录片的特点。这部作品的重要性在于：它进入了被主流叙事遮蔽的历史，让摄影机的镜头照见了一批被排斥、边缘化、甚至被妖魔化的关键证人。观众有幸目睹：当年以各种方式预报了唐山地震的科学家、群测群防小组成员。他们中间的一些人，有汶川大地震以来人们开始耳闻和关注的地震预报专家：七十年代任职地质部地震地质大队的黄相宁、北京地震队的耿庆国、国家地震局分析预报室华北组组长汪成民――他们证明，作为专业人员，他们是深入基础做了调研的，是历经艰难，向有关部门做出预报的。我们还看到，当年山海关一中地震科研小组负责人吕兴亚、唐山乐亭红卫中学地震科研小组负责人侯世钧、唐山二中地震科研小组王书蔚、李伯齐夫妇。他们以简陋的仪器观测地电、磁偏角、水氡的变化，持续不断地做出记录，的确是抓住了地震前一系列宏观异常的。特别重要的证人还有：当年青龙县科委的王春青，是他把汪成民突破封锁释放的临震信息带回县里，使青龙县委做出预防，从而创造了全县无人丧生的奇迹，而青龙县距唐山仅115公里。影片中采用的视听史料还包括录音，这有可能是作家张庆洲先生留存的，它弥补了采访的不足。我们因此听到了其他关键人物的讲述：这里有当年唐山市地震办公室负责人杨友宸，在大震来临之前的5月，他被派去五七干校劳动；对此，地震专家黄相宁愤怒地说，当年的唐山市委书记犯了“过失杀人罪”！还有，当年青龙县县委书记的冉广岐，尽管县里没有权力发地震预报，但他毅然拍板，决定从县到乡传达临震信息。他的话掷地有声：别人拿乌纱帽当乌纱帽，我拿乌纱帽当尿憋子，我考虑的是全县47万人！影片中收集了很多重要的书证资料：例如七十年代的地震界内部刊物、写给国务院的相关文件、中学老师观测用的图纸以及当时发出又收回的简报；这些宝贵的视觉史料具象地表明：在地震日益迫近的时间进程里，地震局的有关领导如何打压异己、忽视预报、最后导致了无可挽回的历史遗憾。王利波的这部作品，除了开头，基本上没有外景，全部都是室内采访。这样做的优点是集中表达口述历史者的观点，缺点是欠缺人物与环境的关联。他用的手段很少，只以淡入淡出遮盖了剪辑点，以字幕提供了时间和事件的关键。尽管如此，我依然认为这部作品非常重要，它填补了我们视觉记忆中的空白；它表现了思考的勇气和敏感。在影片结尾，它记录了这些专家学者对当下地震预测的意见，把尖锐的现实议题留给观众思索：汶川地震之后，如何吸取历史教训？这些专家指出：当年地震局的官员和主流学者，为了推脱责任，完全不承认地震前有预测也有预报；而政府则默认、支持他们。“现在还是这种态度，我觉得这对于我们整个社会是一种很大的危险。” 耿庆国先生说，在发生了唐山大地震之后，“还采用上欺中央下骗百姓的办法，那是不能容忍的，是没有科学家良知的。”针对国外普遍流行的观点：地震不可预测，专家也做出了回答：发达国家走的是抗震这条路，但是不发达国家和发展中国家走不了，因为花钱太多。当年发布信息救了青龙47万人的汪成民先生一针见血地说：“关于地震不能预测，不是在于知识的限制，而是因为管理水平太低。这个水平上不去，好的科学家、知识和技术，仍然发挥不了作用。”地震学家黄相宁先生亦说道：“这次汶川地震的出现和当年华国锋处理唐山地震问题的不彻底有关系，温总理同样犯了华主席当年的错误。这个错误意味着，后面的地震还要继续夺去中国人民的生命。”感谢北京电影学院的崔卫平教授，是她向我推荐了王利波的《掩埋》。在独立纪录片难以生存和传播的环境里，我迫切地感到推荐这个作品的重要。三十年前的悲剧，谁说不会再次降临？《掩埋》揭示出，如果我们不知道灾难在临近，如果那些可能的报信人被扼住喉咙；人人都会因为不知情而被掩埋，而推卸责任的人，注定还要把这样的真相再次掩盖掉。唐山大地震的教训，何尝不是当代中国很多浩劫的缩影？抗震防震，不仅是地震学界的工作，它牵涉到社会治理、公共利益，并有赖于我们地震记忆的重生。因为《掩埋》，我在网上又看到翟明磊先生揭破汶川地震预报谎言的长篇采访：《震旦》；我感到鼓舞的是，在寻找灾难真相的道路上，跋涉已然开始，且势不可挡。<br />
