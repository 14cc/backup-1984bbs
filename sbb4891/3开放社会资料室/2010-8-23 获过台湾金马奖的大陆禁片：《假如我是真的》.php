													<h2>《假如我是真的》：一出未曾公演已被禁的话剧</h2>
						
						
																											<div id="postmessage_793482" class="t_msgfont">作者：黄长怡 郑如煜 <br />
<br />
<img src="https://cmule8.info/attachments/forumid_144/20100823_e74aca31f0ad65731fe92rGYkBsk0Hkk.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
电影版《假如我是真的》剧照。谭咏麟主演。<br />
<br />
&nbsp; &nbsp; 3月底，记者在上海见到了沙叶新。他刚刚从香港讲课回来。最近他百务缠身：写博客，写文章，讲学，编撰戏剧史等等。他的博客每一篇都认认真真地写，或评时事，或谈思想，或讲文化，乐于引起网友的热烈讨论。在接受记者采访的时候，他同时还在接待一位登门拜访的大学教授。这位教授与沙叶新素不相识，在读了他的文章后，万分钦佩他无私的良心和勇气，视他为偶像。在全国，像这位教授一样，对沙叶新一贯以来的大胆直言而心怀崇敬的知识分子，数不胜数。<br />
　　当时的沙叶新，虽然很忙，但精神矍铄，满头黑发，双目炯炯，谈话中处处有他招牌式的幽默，令人难忘。他每天花很多时间散步、锻炼，同时颇自豪于自己从大学时代一直坚持洗冷水澡至今。他健步如飞的样子，让人相信他的身体很好。不过他还是亦假亦真地开玩笑：“装的，就像刘晓庆一样，装嫩。”没想到，一个月后再联系他，他却住进了医院。他发来的短信依然轻松幽默，对生病的事轻描淡写。但他的学生告诉记者，他刚刚动完大手术，马上开始化疗：“是最严重的那种病。”<br />
　　愿好人一生平安。相信这是很多人的心声。<br />
　　“我上台鞠了一躬，表示我向这出戏告别”<br />
　　南方都市报：你是在1978年写这个剧本的吗？<br />
　　沙叶新：我的数字概念极差，连1978、1979都记不清楚。反正是“四人帮”倒台之后写的，《假如我是真的》因为涉及到共产党的弊病，涉及到体制问题，所以在当时引起很大争论。<br />
　　南方都市报：那时候讨论这个问题，压力很大？<br />
　　沙叶新：对，压力比较大，后来中央召开全国剧本创作座谈会，其实主要讨论的就是《假如我是真的》。为了使这个目标显得不是太突出，就拿了几个剧本“陪绑”。提到三四个，但主要还是讨论《假如我是真的》。<br />
　　南方都市报：创作过程是怎么样的？<br />
　　沙叶新：“四人帮”倒台以后，创作力勃发的不止我一个人，整个社会都生机勃发，那几年文学艺术界是少有的一种繁荣和兴盛。这是因为被压制了十年，有很多话想说，有很多苦水想吐，有很多艺术创作欲望都激发出来了。我当时是上海人民艺术剧院的一个普通编剧，剧院的院长黄佐临很开明，鼓励和支持年轻编剧投入创作，我们剧院的创作环境、工作环境都比较好，我感谢上帝能让我进入他领导的剧院。<br />
　　南方都市报：你是毕业之后就进了这里吗？<br />
　　沙叶新：对，我是华东师范大学中文系毕业，然后到上海戏剧学院读研究生。读研后先是被分配在上海市文化局的一个理论研究室，从事理论研究，后来才被我们院长要到了他那里。<br />
　　在毕业之前，我还跟姚文元打了一个很大的笔战。我写了《审美的鼻子如何伸向德彪西》，当时全国文艺界都知道这件事情，我受到批判。据一位叫朱琨的老先生说，黄佐临老师很欣赏我。《审美的鼻子如何伸向德彪西》发表那天，黄院长从三楼去到二楼朱琨的办公室将一张报纸轻轻地扔在他的桌上，说：“你看，小沙的文章！”非常自豪、得意。<br />
　　后来我读研毕业之后，黄佐临很爱才，“世人皆欲杀，吾意独怜才。”所以他对我有知遇之恩。那时是全国批判我这个23岁的人，铺天盖地的报纸，《光明日报》、《文汇报》发表文章纷纷批判我，那个集子那么厚，而黄院长他竟然把我要到了他的身边。我23岁时遇到黄佐临，他决定了我一生的命运。黄佐临留学过英国，在皇家学院毕业，1949年以前，他在上海从事受左翼影响的戏剧活动，但是他更倾向于艺术为人生、艺术为艺术，而不是艺术为革命，而且他还喜欢喜剧。<br />
　　南方都市报：你怎么想到这出《假如我是真的》？这是一件真实的事情吗？<br />
　　沙叶新：那时，社会上流行着一个故事，就是有一个骗子，小青年。他冒充自己是将军李达的孩子，然后他就在上海行骗，很多人都被他骗了。<br />
　　这是真事，充满了戏剧性，被骗的人有一位现在还健在，是一个德高望重的老干部；他还骗了一些文艺界的著名人物和其他一些人。打倒“四人帮”之后，很快地干部层面的不正之风就已经冒头。比如搞特权、想把自己的孩子从乡下调回来、增加自己的房屋面积、开后门这些现象已经有了，但是跟现在的腐化没法比。其实故事的戏剧性，就在于小骗子说自己是高干子弟，很多人就拍他马屁。他利用这些人的私欲，和他们做交易。这个人聪明绝顶，二十多岁，我见过他，在监狱里。<br />
　　南方都市报：听说你还跟公安一起去审问他？<br />
　　沙叶新：对，但是我不能说话，这涉及到公安的纪律，我只能在旁边像书记员一样旁听。根据这个我就想写一部戏，可是并不只有我一个想写。我们上海有十大剧院，越剧院、沪剧院等等，很多剧种的编剧都在采访，收集资料，都想以这个题材来写。可不久突然这些人都不写了，因为上面有指示，进行干预，不准写了。由于涉及到一个还在位的上海老干部，影响不好，不让写了。<br />
　　其实我也有过不想写的过程，我不写是因为知道大家都在写，我就不想写了。后来说大家都不写了，他们不写，我就非要写不可了。<br />
　　南方都市报：有没有人给你打招呼？<br />
　　沙叶新：没有，主要是因为我所在的思想环境和工作环境比较好。经过采访后，我就和我的二位合作者躲在无锡话剧团的团部那里写，因为这样可以减少干扰，比较安静。我们很快就写好了，三个星期就写好了吧。我和另外两个人一起写，一位叫李守成，一位叫姚明德，主要是我执笔。我那时候是专业编剧了，他们二位是演员。<br />
　　南方都市报：你那时候年纪多大呢？<br />
　　沙叶新：应该是三十岁出头吧。写了之后，我们去团里念，哇！反应极为强烈。我们这个剧团还是有正义感的，演员都说要排，很强烈的呼声。<br />
　　南方都市报：但是那个时候你已经知道上面打过招呼不要写了，你们不会有这方面的顾虑吗？<br />
　　沙叶新：没有顾虑。“四人帮”倒台之后，在精神上获得一个大解放。我觉得在“文革”中给我最大的教训就是，脑袋一定要长在自己的脖子上，绝对不能盲从。当时整个社会都在思想解放，都在沉痛地认真反思，我们国家怎么会碰到这么大的政治灾难呢？这到底是什么原因呢？中国共产党在思考，老百姓也在思考，我们知识分子、作家、艺术家也在思考。“四人帮”倒台以后，我们剧院也是处在这样一个思想解放的过程中，剧院领导很支持，演员很支持，很快就排出来了。这出戏写得很快，两个星期，排得也很快，只有半个月。领导、编剧、导演、演员、全体舞台工作人员，上下一心，团结一致充满激情地把这个戏排出来。<br />
<br />
<br />
<br />
 <br />
<br />
<br />
<br />
　　剧本写出之后，为了避开审查剧本的麻烦，我提出先不审查，直接让剧本和导演、演员见面。我声情并茂地朗读完毕，获得一片掌声。导演胡思庆同志立即站起来，向领导请战，要求由他来导演这出戏。这出戏后来取得巨大的成功，是和胡思庆的勇敢与天才分不开的。以后我的《陈毅市长》也是他导演的，也同样取得成功。让我们记住这位英年早逝的戏剧天才。演出之后，反响巨大。<br />
　　南方都市报：当时是先在上海演吗？<br />
　　沙叶新：先在上海演，一演出就引发了争论。前排坐着的都是领导干部，大都表情严肃沉默无语。后面坐着的是一般普通百姓，反应热烈，拼命鼓掌。在1949年一直到《假如我是真的》出来前这段时间里，在中国的剧坛上很少有一部戏敢于把干部身上的缺点、弱点作为批判对象的。一个共产党的剧院怎么可以演出批评共产党的戏呢？坐在前面的观众，是领导，是干部，他们出于根深蒂固的“党性”，不敢表示支持。后面的老百姓出于一种正义，出于一种热情，就拼命地鼓掌。宣传部也觉得对这个戏来讲是个新经验，很难表态。<br />
　　南方都市报：当时没有先内部试演一下，再对外公开演的吗？<br />
　　沙叶新：这个过程应该有的。先彩排，预演引发了激烈的争论，然后就有了一个折中的办法，就是内部演出，不登报，一直没有公开演出。<br />
　　我们就希望公开演出，这跟黄佐临的支持很有关系。但是，我相信那个时候上海人民艺术剧院的党组织也是支持这个戏的，要不然光是黄佐临一人支持，大概也很难演成。后来就达成了一个协议，就是内部演出，照样卖票，但是就是不登报。<br />
　　南方都市报：是啊，所以我能找到的东西很少，除了剧本。<br />
　　沙叶新：可这个戏后来在全国普遍都演了，比如福建、广东、浙江、江苏、河南、河北包括新疆也演了。不宣传都这样的，登报宣传的话那还得了？这说明这个戏至少在当时，是代表民意、反映民心、反映群众呼声的。还有一点，这个戏是喜剧，可看性比较强。好久没有看过这样的戏了，就是看了能使人哈哈一笑而且让人深思的戏。这也是群众喜欢的原因之一。<br />
　　南方都市报：当时在上海内部卖票演了多少场？<br />
　　沙叶新：几十场会有吧。反响都是很强烈的，很多人要票，北京也演了。<br />
　　巴金先生在他的出版在香港的《随想录》中，有三篇文章谈到《假如我是真的》，题目好像是《小骗子》、《再谈小骗子》之类的，都是谈到这个戏。他在文章当中说没有看过这部戏，其实他看过。黄佐临告诉我的。巴金对这出戏的态度很明显。后来我加入中国作协，受到一点阻力，巴金毅然当我加入中国作协的介绍人，这也可以看出他的态度。<br />
　　南方都市报：这出戏一直没有公演？<br />
　　沙叶新：没有，一直是内部演出，演了几十场。后来迫于压力还是停演了。我记得停演那天，我还上台“向遗体告别”，也就是向这出戏告别。那天是在建工局礼堂演出，演出结束时我坚持一定要上台和观众说几句话，舞台监督和剧导演有点紧张，都担心我闹事。我记得我上台鞠了一躬，表示我向这出戏告别，在新中国的演剧史上，一个剧作者为他被禁演的戏公开上台表示某种抗议，这是第一次，史无前例！<br />
　　南方都市报：这出戏最终停演的原因是什么？<br />
　　沙叶新：宣传部门不能让这种“给共产党抹黑”的戏再演下去。往深里说，是因为这出戏涉及到体制问题，并不光是在浅表层面批判干部的特权，有些人难以接受。我们本来找不到好的名字，一开始叫《骗子》，而且那些不喜欢这部戏的人也希望叫《骗子》，黄佐临画龙点睛地为此剧取为《假如我是真的》，便是对“体制”的尖锐提问，假如我真的是李达将军的儿子呢？那不就什么事也没有了吗？就不是诈骗、不是犯罪了吗？这说明根本问题不在于骗子，而在于体制。<br />
　　“我的入党，由胡耀邦亲自批示”<br />
　　南方都市报：停演之后呢？发生了什么事？<br />
　　沙叶新：就是我刚才提到的，召开了一个全国剧本座谈会，其实就是针对《假如我是真的》这部戏的。从1949年到那时，还从来没有出现过专门为一出戏召开一次全国性的座谈会的情况。当时我去了，但黄佐临没去。因为他们知道黄佐临支持这出戏，怕他分量太重了，如果在大会上说出什么话来不好处理。我去北京开会前，佐临把我叫到二楼半一间小办公室，拿出一盘录音磁带，亲手交给我，他说：“小沙，你拿会上去发，里面是我的发言！”他是院长，是此剧最大的当事人，居然这样的会没让他去，可见这位一向谦和的老艺术家以一盘录音带所表达的激愤和抗议。<br />
　　南方都市报：会上情况如何？<br />
　　沙叶新：争论，这个会是胡耀邦主持的，他当时是宣传部部长。虽然会上一些人把我批评得很严厉，但我看得出胡耀邦的苦衷，他是保护我的，他不希望打击我这个小年轻。所以他在会上介绍我的时候，把我说得特别高，什么“当代的莎士比亚”之类。我很清楚自己的分量离他说的很远，我知道他是想尽可能地维护我，怕伤害了我。<br />
　　开完这个会后我写了《扯“淡”》那篇文章。别人一听名字就觉得很敏感，其实我这个“淡”字有个引号，就是淡季和旺季的“淡”。北京的剧本座谈会开了以后，原来的戏剧很旺盛的，一下子就淡下去了。别人就说，你这是反对胡耀邦嘛！我说我没反对胡耀邦呀，胡耀邦自己在大会讲话中也说了：“我的讲话，不是命令，不是指示，我说的是一点意见，意见是可以讨论的。”我响应胡耀邦同志的号召，写文章与他讨论怎么就是反对耀邦了呢？这才是真正的扯淡哩！<br />
　　南方都市报：你入党是什么时候的事？<br />
　　沙叶新：大概快进入1985年的时候，当时黄佐临要退休了，剧院没有人接这个盘。那时比较民主，经过党内党外背对背的调查，我的呼声很高。并不是因为我有很大的才能，而是我比较老实，当了官以后不会贪污腐化。我二十多岁就进了剧院，经过“文化大革命”，我没有出卖过人，没造过反，大家还觉得我这个人比较用功，所以呼声比较高。黄佐临也想让我接他的班，成为院长。可是要当共产党的干部的话，不是党员怎么行？<br />
　　南方都市报：你那时还不是党员？<br />
　　沙叶新：不是。黄佐临就劝我入党。假如我投机的话，马上就打入党报告了。可是我回信说，你让我想一想，这件事很慎重的。三个月之后我才跟黄佐临说，好的，我可以争取入党。我想我今后能和邓小平、胡耀邦在同一个队伍里，在剧院能和佐临以及那么多优秀党员在同一个队伍里，是非常光荣的事，这样我就写了入党申请书，可是就是不批准。不批准的不是我们剧院，我们剧院已经通过了。就像我加入作协也很困难，我加入作协的介绍人是巴金。我们剧院的部分党员，就写信给上海市委统战部的知识分子处，知识分子处就给胡耀邦同志写了一封信，还把我院部分党员给耀邦的信交给耀邦的秘书，后来胡耀邦同志批示了，也向我传达了，我印象很深刻。批示有一个总批：“该同志够入党条件，就应该让他入党。”起码是肯定的意见吧。还有一个侧批，就说到《扯“淡”》这个问题。耀邦同志的批示写着“该文与我无关”，意思说这篇文章不是反对我的。这批示下来第二天还是第几天吧，我沙叶新就成了光荣的共产党员了。<br />
　　南方都市报：够快的。<br />
　　沙叶新：第三天吧，全国头版头条发了新华社的通稿，报道我的，标题挺吓人，《党的作家党的人》。一个入党第三天的人，就变成了“党的作家党的人”了，这评价太高了，太搞笑了。一个普通编剧入党还惊动了党中央书记，又实在太荒唐了。<br />
　　《陈毅市长》到中南海演出，耀邦同志来看了。看完还找我，可是那时候我不在，到安徽去了。后来别人跟我说，耀邦同志看完戏上台跟剧组人员握手时，就问：“叶新沙呢？叶新沙呢？”他把我的名字记成叶新沙了。还有人开玩笑说，要是在封建社会，这可就是皇帝御赐的名字啊，那我就非改姓叶了。这是关于胡耀邦的几件事。你可以感觉到他的开明、民主，一个普通的小编剧入党，他都有批示，而且在看《陈毅市长》时还没有忘掉我。<br />
　　南方都市报：《陈毅市长》是在《假如我是真的》后写的吗？很多人都以为《陈毅市长》是赶紧“补过”写的。<br />
　　沙叶新：不是，是先写的《陈毅市长》。其实沙叶新还不会这么投机。《陈毅市长》是先写的，就是没有演。当时我们的罗副院长不同意演这出戏，因为写陈毅老市长是一件重大严肃的事情，我却用和正式戏剧不太一样的形式，他接受不了，没有排演。后来就写了《假如我是真的》。其实这两个戏的宗旨是一样的。一个就是说共产党不应该怎么样，一个就是说共产党应该怎么样。一个表示批判，一个表示我的肯定。爱和恨是相通的。这两部戏让我开始进入剧作家的队伍。<br />
　　南方都市报：现在大家提起你还是会说这两部戏。<br />
　　沙叶新：人家就说，沙叶新真会写啊，左的能搞，右的也能搞，好厉害啊！其实一个作家就是不能滑头，不能投机。而且我认为人品和作品真的分不开，虽然我做得很不好，但是我会时时提醒自己。其实我以后的剧本比这两出写得好，如《耶稣・孔子・披头士列侬》、《江青和她的丈夫们》、《幸遇先生蔡》，但在国内的影响不及这两出，因为不让演，只演了一部《耶稣・孔子・披头士列侬》。<br />
　　南方都市报：那次座谈会有没有给你定调子？<br />
　　沙叶新：没有，只是希望我修改。我内心很抵抗，就是不修改，我们三个作者的态度基本一致。具体要我们改成什么样我就忘了，无非是要突出党的正面力量，要看出绝大多数干部是好的，不能突出反面人物，不能同情骗子等等。<br />
　　南方都市报：你就是演不了，也不改？后来就没有再演了？<br />
　　沙叶新：没有再演了。后来很快台湾那边就把它拍成了电影。这部电影是谭咏麟主演的。当时，谭咏麟在上世纪八九十年代的名气比现在的周杰伦还要大，天皇巨星。可是他跟我说，在演《假如我是真的》之前，他并不出名。他第一次到上海来演出，刚下飞机，就跟记者说，我能不能见沙叶新？<br />
　　他见了我，说：我就是因为演你的戏之后才出了名，才转了运，出了名就不停地得奖。他认为：“你给我带来了好运气。可是你却因为写了这个戏反而在大陆遭罪，所以我要请你吃饭。”我就觉得这个人蛮好的，很讲义气。我记得我的女儿问我：“爸爸，谁请你吃饭啊？”我说谭咏麟。那时她她正在念高中，激动得很：“你知道这意味着什么吗？”<br />
　　南方都市报：电影主题歌是邓丽君唱的对吧？<br />
　　沙叶新：邓丽君唱的。所以那时在国内间接跟邓丽君合作过的只有我一个人，没有其他人。<br />
　　南方都市报：在台湾演的时候剧本有没有大的修改呢？<br />
　　沙叶新：基本上改动不大。只是因为一个是话剧，一个是电影剧本，细微的地方肯定有所改动的。这部电影得了第18届金马奖的“最佳剧情奖”。这里面有一笔稿费，说等有机会把钱交给沙叶新或者他的家属。<br />
　　南方都市报：你拿到这笔钱了吗？<br />
　　沙叶新：拿到了。那时候对我来说是一笔巨款，但具体数字我又记不清了，大概几十万吧，我们三人分。另外还有利息，放在兴业银行，由制片厂交给台湾的某机构，大概是文宣机构吧。后来托一个台湾朋友，签了一个委托书给他，那时候还没有护照，就把我的身份证复印件传给他，在那里得到确认，就把钱带到了香港。我就到香港去拿了。<br />
　　南方都市报：够麻烦的啊。这个剧本后来还在其他地方正式发表吗？<br />
　　沙叶新：然后翻译成英语、德语，在国外发表也演出了。<br />
　　南方都市报：在台湾包括国外上演不是更敏感吗？<br />
　　沙叶新：是呀，比如台湾在拍这部电影的时候，有关方面就希望我出面发表声明，来阻止他们拍摄。我当时心里很矛盾，虽然我希望台湾排成电影，可是又怕他们添油加醋，把我的剧本改成一个反共的剧本。另外，那时我们也很单纯，认为国家的话、党的话不能不听啊，我们三个就同意发表声明。我们的声明是从版权的角度反对他们拍，就说你们改编的剧本没有经过我们的同意，所以我们表示抗议。后来发表在香港的几家报纸上面，当然都是我们的左派报纸，没有用的。人家一看就知道这是作者在某种压力下的声明，不会听你的，阻止不了人家拍成电影的。<br />
　　南方都市报：你刚才提到黄佐临喜欢喜剧，对你的影响很大。像《假如我是真的》这种题材，如果写成“义愤填膺”的正剧，也是一种写法。你为什么选择把它写成讽刺性的喜剧？<br />
　　沙叶新：这和一个人的文化基因有关系，也和性格有关系。我觉得人生下来应该是快乐的，应该是享受生活的。我们中国有个艺术传统叫寓教于乐，它虽然主张教化，主张文以载道，但对戏剧还有希望寓教于乐。中国有很强的喜剧传统，从“优孟衣冠”就开始了，1949年以后因为政治体制和文化环境不一样了，它就寓教于政治，寓教于严肃了，特别是话剧。你看样板戏没有一个可以让人笑起来的。<br />
　　南方都市报：我觉得你的作品从小说一直到剧作，很有欧美文学的风格。你大学的时候写过两篇小说，有一篇叫《老鹰篮球队》。<br />
　　沙叶新：还有一篇是《美国剧院的悲剧》。欧・亨利对我影响很大，特别是在结构上。奇峰异起，但是又合情合理。马克・吐温对我的影响也很大，因为他幽默。老舍的所有剧本我都读过。喜剧，包括相声、滑稽戏，没有直面人生的讽刺就必定死亡。</div>

							
							
							
							
