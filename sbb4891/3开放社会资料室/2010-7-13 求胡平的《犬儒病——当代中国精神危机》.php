																											<div id="postmessage_737305" class="t_msgfont">犬儒病――当代中国的精神危机<br />
<br />
胡平<br />
<br />
　　　　人心不会熄灭，但它可能蒙上灰烬而不再燃烧。灰烬本来是燃烧的产物，但它反过来又抑制了燃烧。 　　　　拨开灰烬，你会看到重新燃烧的人心。 　　　　　　　　　　　　　　　　　　　　　　　　　　　　　　　　――作者手记<br />
<br />
　　　　当今中国最流行的是什幺主义？ 答：犬儒主义。<br />
<br />
1、犬儒主义的来龙去脉<br />
<br />
　　犬儒主义（Cynicism）是个外来词，中文里本来没有现成的对 应词汇，通常将它理解为讥诮嘲讽，愤世嫉俗，玩世不恭。这些理解大致不差，不过，我们若想对犬儒一词有更完整的把握，有必要略略追溯一下它的起源和演变。<br />
<br />
　　我们知道，犬儒主义是古希腊的一个哲学流派，其代表人物是西诺普的狄奥根尼（Diogenes of Sinope）。这派哲学主张清心寡欲，鄙弃俗世的荣华富贵，力倡回归自然（这使人想起老庄哲学，想起某些魏晋名士）。据说狄奥根尼本人住在一个桶里（又有一说是住在瓮里），以讨饭为生。有人讥笑他活得象条狗，他却不恼。“犬儒”之称由此得名。关于狄奥根尼，有段故事很著名，一天，亚历山大御驾亲临，前来探望正躺在地上晒太阳的狄奥根尼，问他想要什幺恩赐；狄奥根尼回答说：“只要你别挡住我的太阳。”<br />
<br />
　　和玩世不恭恰恰相反，早期的犬儒是极其严肃的，狄奥根尼是一个激烈的社会批评家。他立志要揭穿世间的一切伪善，热烈地追求真正的德行，追求从物欲之下解放出来的心灵自由。狄奥根尼确实愤世嫉俗，他曾经提着一个灯笼在城里游走，说：“我在找一个真正诚实的人。”<br />
<br />
　　随着犬儒哲学的流行，其内涵开始发生了微妙的变化。后来的犬儒派们发表宏论，竭力鼓吹清贫生活的无比美好，甚至把人们正常的感情也讥为愚蠢。一位名叫德勒斯（Teles）的犬儒派就说：“我的儿子或妻子死了，那难道就有任何理由应该不顾仍然还在活着的我自己，并且不再照顾我的财产了幺？”（这使人想起庄子死了老婆鼓盆而歌的故事）超脱到了这一步，未免就有些矫情，就和冷酷分不清界限了。还是这位德勒斯，某富翁送给他一笔钱，他收下了，对富翁说：“你慷慨大度地施舍给我，而我痛痛快快地取之于你，既不卑躬曲膝，也不唠叨不满。”这里暗含的逻辑是，金钱本是无所谓的东西，我若拒绝你的馈赠，倒显得我把金钱看得太重，太当回事了。我若收下金钱又表示感谢，那也是把金钱看重了，当回事了。因此，正确的做法就是，只要你肯给，我就若无其事地收下它。不要白不要，要了也白要。这种态度看上去很洒脱，但好象又有些无耻。这到底是怎幺一回事呢？问题在于，金钱本来是重要的东西，不是不重要的东西。只不过在生活中还应该有别的东西比金钱更重要。所以，在坚持更高价值的前提下看轻金钱是高尚的；没有更高的追求却又摆出轻视金钱的姿态就不是高尚而只能是做作了，因此，倘若是无功受禄，正常人总会感觉不安。这就是为什幺德勒斯以不把金钱当回事为理由而若无其事地收下别人的馈赠，会给人以不知廉耻之感。早期的犬儒派是依据一种道德原则去蔑视世俗的观念，后期的犬儒派依然在蔑视世俗的观念但却失去了依据的道德原则。这就引出了一个始料不及的后果：既然无所谓高尚，也就无所谓下贱。既然没有什幺东西是了不得的，因而也就没有什幺东西是要不得的。不难想象，基于这种无可无不可的立场，一个人可以很方便地一方面对世俗观念做出满不在乎的姿态，另一方面又毫无顾忌地去获取他想要获取的任何世俗的东西。于是，对世俗的全盘否定就变成了对世俗的照单全收，而且还往往是对世俗中最坏的部分的不知羞耻的照单全收（别充假正经）。于是，愤世嫉俗就变成了玩世不恭。<br />
<br />
　　狄奥根尼坚持真善，揭穿伪善，这种批评精神被后来者扭曲得面目全非。一位人称嘲讽者吕西安（Lucian the Mocker）的犬儒派以揭穿伪善的名义，压根否认世间存在有真善。在吕西安笔下，那些天真地追求德性的人都不过是大傻瓜而已。按照这派人的看法，世间之人只有两种，要幺伪君子，要幺真小人。犬儒一词后来的含义就是把人们一切行为的动机都归结为纯粹的自私自利。不错，犬儒派既嘲讽有权有势者，也嘲讽无权无势者，但前者并不在乎你的嘲讽，“笑骂由人笑骂，好官我自为之。”后者却必须赢得人们的同情支持。所以，犬儒派客观上是有利于强势者不利于弱势者。这样，犬儒派就从现存秩序的激进批评家变成了既得利益者的某种共犯合谋。<br />
<br />
　　犬儒一词的演变证明，从愤世嫉俗到玩世不恭，其间只有一步之差。一般来说，愤世嫉俗总是理想主义的，而且是十分激烈的理想主义。玩世不恭则是彻底的非理想主义，彻底的无理想主义。偏偏是那些看上去最激烈的理想主义反倒很容易转变为彻底的无理想主义，其间原因何在？因为，许多愤世嫉俗的理想主义者在看待世界时缺少程度意识或曰分寸感，对他人缺少设身处地的同情的理解，不承认各种价值之间的紧张与冲突，这样，他们很容易把世界看成一片漆黑，由此便使自己陷入悲观失望，再进而怀疑和否认美好价值的存在，最终则是放弃理想放弃追求。“世界既是一场大荒谬、大玩笑，我亦唯有以荒谬和玩笑对待之。”一个理想主义者总是在现实中屡屡碰壁之后才变成犬儒的，但正如哈里斯（Sydney J．Harris）所言：“犬儒不只是在过去饱尝辛酸，犬儒是对未来过早地失去希望。”<br />
<br />
　　说来颇具讽刺意味，早期的犬儒是坚持内在的美德和价值，鄙视外在的世俗的功利。可是到后来，犬儒一词正好变成了它的反面：只认外在的世俗的功利，否认内在的德性与价值。王尔德（Oscar Wilde）说：“犬儒主义者对各种事物的价钱（price）一清二楚，但是对它们的价值（value）一无所知。” 　　<br />
<br />
2、专制与犬儒<br />
<br />
　　密尔（John S．Mill）早就指出，专制使人变成犬儒（这使人联想起王夫之的话：“其上申韩者，其下必佛老”）。共产专制既是一种极端的专制，因此它更会使人变成犬儒。在共产专制下，统治者与被统治者都容易变成犬儒。统治者变成犬儒，因为他们早就不相信他们口头上宣讲的那套理论和原则，他们只把那些理论和原则当做维护权力的手段以及镇压反抗的借口。在被统治者方面，当他们一旦意识到自己在冠冕堂皇的旗帜下实际上处于被愚弄被压迫的境地，很容易转而对一切美好的价值失去信心。尤其是在试图反抗又遭到严重的挫折之后。这样，他们就可能放弃理想，放弃追求，甚至反过来嘲笑理想，嘲笑追求――吃不着葡萄就说葡萄酸。这样，他们就变成了犬儒。当然，统治者的犬儒主义和被统治者的犬儒主义是有所不同的，但是广义地讲，它们都可以归入犬儒主义。<br />
<br />
　　按照欧文・豪（Irving Howe）的分析：极权主义有三个阶段，（一）、乌托邦，令人心醉神迷的天堂理想，它诱发了狂热，而狂热则导致了（二）大规模的恐怖和人间地狱，然后，狂热与恐怖被耗尽，于是，（三）、人们变得玩世不恭，“看透一切”，政治冷感，即犬儒主义。<br />
<br />
　　我以为极权主义有四个阶段，在狂热和恐怖之后常常还发生过反抗，在反抗受挫之后才会出现普遍的犬儒主义。极权统治靠人们的狂热而建立，通过大规模的恐怖而得以巩固，但是，狂热和恐怖都不可能持久，最后是靠着人们的消沉与冷漠，极权统治才得以维系。<br />
<br />
3、史密斯笔下的俄国人<br />
<br />
　　美国记者赫德里克・史密斯（Hedrick Smith）在他那本写于一九七六年的的&lt;俄国人&gt;一书中，向读者讲述了在勃烈日涅夫时代弥漫于苏联社会的犬儒主义。我们知道，自苏共二十大赫鲁晓夫批判斯大林后，苏联社会出现了所谓“解冻”即有限的自由化时期。随着自由化运动的深入推进，苏共当局重新加强控制。其后，赫鲁晓夫被黜，勃烈日涅夫上台，进一步压制自由化运动，致使该运动渐趋沉寂。正是在这种情势下，犬儒主义蔓延，构成当时社会的一个显着特征。<br />
<br />
　　史密斯发现，在苏联，共产主义意识形态实际上已经很少有人相信，首先是苏共领导人自己就不再相信（顺便一提，不久前，勃烈日涅夫的侄女柳芭发表回忆录，其中写到，勃烈日涅夫当年曾对自己的弟弟说：“什幺共产主义，这都是哄哄老百姓听的空话。”）史密斯引用一位莫斯科的科学家的话，“意识形态可以起两种作用――或者是作为一种象征，或者是作为一种理论，两者不可得兼。我们的领导人把它用来作为一种象征，作为断定其它人是否忠诚的一种方法，但它并不是这些人身体力行的一种理论。它不是活的理论”。好比赵高在金殿上指鹿为马，以此测试群臣，看谁是跟自己的谁是不跟的。一位高级编辑指出，现今当政的这些苏共领导人是没有信仰的人，“是一些对一切都无所谓的人。他们所要的是权力，纯粹是权力”。这位编辑说，虽然上上下下的人都不再相信官方的意识形态，而且对各种事情也并非没有自己不同的看法，但是一到了正式的场合，他们却照旧举手拍掌，重复着官方的陈词滥调。人们明知这一切是毫无意义的，是逢场做戏，“可是你必须去玩它”。<br />
<br />
　　许多俄国人既然抱着看破红尘的态度，因此，当他们发现这个世界上竟然还有人真的坚持某种理念，某种理想主义，简直觉得惊奇。在一次国际和平大会上，一位苏联代表团的成员就对美国代表的较真深感意外。他私下问史密斯：难道这些美国人真的认为他们能够发挥作用，能够影响现实政治吗？<br />
<br />
　　史密斯认识一个苏共少壮派官员。看上去此人是充满矛盾的复合体。他一方面在和朋友谈话中批评时政，攻击腐败，俨然是个改革家；另一方面，他又对本国的政治感到自豪，为自己能身处权势集团而踌躇满志。他清楚地知道斯大林时代的恐怖，也不愿意再回到那个时代，但与此同时，他又对斯大林靠强权建立起一个庞大的红色帝国而十分骄傲。一方面，他很乐意向别人显示他的思想解放，根本不相信官方的教条。另一方面，他又对自己善于掩盖个人观点，对自己在党内会议上以善于发言着称而得意。其实，这正是苏共新一代官员的一种典型――无信仰的、犬儒式的机会主义者。“可见，”史密斯总结道，“个人只要服从听话，不公开向意识形态挑战，不管信也好不信也好，都不是关键问题。”<br />
<br />
　　伴随着看穿一切的思潮的流行，物质主义也开始泛滥。这后一点倒也情有可原。经过了半个多世纪的折腾，俄国人已经付出了太大的代价，共产党许下的诺言又在哪里？无怪乎人们会这样想：“人只活一世，而这一世是短促的。所以，请给我一点东西吧。别老是许给未来呀！”物质主义的泛滥进一步冲掉了残存的理想主义。许多人为了一点点物质利益――为了一次出国机会，为了分得一套房子或搞到一部新汽车――甘愿放弃自己的独立政见。这样，当局无须乎再采取大规模的恐怖措施，就足以控制住它治下的广土众民。<br />
<br />
　　少数勇敢的持不同政见者依然在发出他们的声音。在起初一段时期，他们赢得了广泛的尊敬，虽然敢于公开表示这种尊敬的人不多。然而令人惊异的是，到后来，当局对异议人士泼污水，在一部分人中间竟然也得到某种响应。史密斯对此大惑不解。一位名叫瓦连京・图尔钦的异议人士对他解释说：“人群中有一种难以相信的犬儒主义。诚实的人使得那些沉默的人由于没有大胆说话而有负罪感。他们无法了解别人怎幺会有勇气去干他们本人所不能干的事。因而他们感到不得不攻击别人以安慰自己的良心。第二，根据他们自己的经验，他们觉得每一个地方的每一个人，都在欺骗自身之外的每一个人。苏联人好象妓女一样，因为自己是妓女，便认为所有的女人都是妓女。苏联人认为整个世界是分为党派的，每一个人都是这个党或那个党的成员，根本没有真正的诚实，根本没有人支持真理。如果有人说他是公正的，是只讲真理的，那幺，他就是在说谎骗人。这种犬儒主义给当局帮了大忙：使知识分子就范，把不听话的异议人士排斥于社会之外。虽然人们可以到西方去旅行和收听西方的电台。但只要普遍存在着这种犬儒主义，他们就会认为那不过是另一派在说话，所以也就值不得当真了。这种犬儒主义提供了极权国家今天的稳定，以代替斯大林时期的大规模恐怖。”<br />
<br />
　　在新形势下，正象一位数学家讲的那样：“提倡玩世不恭是控制的基本方法。”<br />
<br />
4、共产党与犬儒主义<br />
<br />
　　严格地说，犬儒病的发生并非只在共产专制的末期。早在共产革命兴起之初，犬儒的病毒就已经侵入其中。<br />
<br />
　　从一开始，共产党便与犬儒主义结下不解之缘。<br />
<br />
　　共产党的意识形态是一种极其高调的理想主义，但与此同时，它也是一套相当阴暗的犬儒主义。准确地说，它是理想主义与犬儒主义的奇特结合。一方面，共产党立志要一劳永逸地结束世上一切苦难与罪恶，在人间建成天堂。这自然是理想主义。另一方面，共产党又认为，既然他们的目的是如此崇高美好，还有什幺手段是不可以采用的呢？因此，共产党可以一方面对所谓旧社会旧制度发动极其苛刻的道义讨伐，另一方面又在自己的行为中罔顾最起码的道德约束。这显然又是犬儒主义。一方面，共产党把人的理想悬得极高，另一方面，它又把现实的人看得极低。它对未来人类的看法之所以高度的理想化，恰恰是因为它对现实的人类的看法是十足的犬儒式。共产党的阶级分析理论和阶级斗争理论，实际上就是认定人是彻底自私的动物，利己是人类行为的唯一动机；由此，共产党断言在社会分裂为不同的阶级的情况下，不可能存在有普遍的同情心和正义感，超阶级的自由民主只能是一场骗局。但只要通过彻底的共产革命，消除了阶级存在的社会物质根源，全体人类的利益便达到充分的和谐一致，利己与利他的矛盾便不复存在，利己主义也就随之消失，具有最完美人格的共产主义新人也就随之出现。在这里，前提是犬儒主义的，结论却是理想主义的；或者说，理想主义的结论却是导自犬儒主义的前提。共产党自命为无产阶级的先锋队。一方面，共产党宣称，除了无产阶级的利益、人民大众的利益，共产党别无任何自己的特殊利益。这看上去很谦虚，也很富理想主义。但是另一方面，共产党又坚称唯有它自己才“代表”了无产阶级，“代表”了人民大众，而被它代表的无产阶级和人民大众本身倒未必有资格算得上无产阶级，算得上人民大众（列宁明确宣布，无产阶级不能自发地产生无产阶级世界观，必须由共产党从外部将之灌输给无产阶级）。这不只是理性的狂妄僭越，更严重的是，它为那些骨子里什幺也不信的犬儒主义者们攫取无限权力大开方便之门。所以，并不奇怪，尽管共产党以它的理想主义大旗召唤了许多理想主义者投身其间，但是在共产党内部，理想主义者们往往不能占上风，倒是那些犬儒式的机会主义者们更吃得开。<br />
<br />
　　讲到共产党、尤其是中国共产党的犬儒主义，我们还必须提到它的武装斗争的历史。不要忘记，中共是经过二十多年的武装斗争才夺得政权的。和中共的武装斗争历史相比，俄国的十月革命差不多只能算是一场武装政变，乱中夺权。一般来说，假如暴力革命经历的时间越长，斗争越残酷，在这一过程中，革命党人就越是容易把革命的初衷置诸脑后，越是把胜利、把权力当作狂热追求的最高目标，以至于到后来，他们不再把权力当作实现理想的手段，而是反过来把理想当作夺取权力和垄断权力的手段。因此，他们就很容易从理想主义者蜕化为犬儒主义者。据说在六四之后，中共某元老讲过一句话。他说：“共产党的江山是牺牲了三千万人的生命才换来的。谁要想推翻它，谁也要付出同样的代价。”乍一看去，此话甚是不通，当初共产党闹革命不是为了让人民当家作主吗？如果人民要求政治改革，结束一党专政，实行当家作主的权力，共产党又有什幺理由拒绝呢？不过，你没法用这话去驳倒那位中共元老，因为他们早就把什幺人民当家作主的理想扔到九霄云外了。<br />
<br />
　　我们知道，从一开始，共产党就否定议会道路，主张暴力革命。这本身就表明它不相信民主，其实也就是不相信人民。一般来说，革命者决心使用暴力实行他们的主张，那不仅表明他们对统治者不抱幻想，同时也表明他们对人民感到失望，失望于人民的所谓愚昧无知与自私怯懦。道理很简单，如果没有一定数量的民众向统治者认同，为统治者效力，如果没有相当数量的民众在政治上冷漠麻木，袖手旁观，统治者就成了孤家寡人，如何还能成功地抗拒改革呢？主张暴力革命的孙中山就不讳言他对中国国民的素质估计很低，所以他提出在革命胜利后要通过军政、训政和宪政三个阶段逐步实现民主。共产党的情况则还要复杂得多。一方面，共产党更不相信人民。在漫长而残酷的革命岁月中，共产党历经多次失败，几度濒于绝境。不难想象，他们该有过多少孤寂，多少沮丧，多少怨愤。这就容易使他们对民众、对人性的看法变得十分阴暗。然而，靠着那套先锋队的理论和无产阶级专政的理论，他们很可以把自己对人民的失望以至蔑视掩藏在一层看上去理想主义的外衣之下。另一方面，共产党又发现，国民党的不民主为他们攻击国民党提供了堂皇的理由，而国民党专制的不彻底又正好为这种攻击提供了现成的空间。于是，他们就摆出了民主斗士的姿态，利用国民党专制的不彻底，抨击国民党的不民主，自己却早就打定了要更专制的主意。<br />
<br />
　　在八一年，针对着一些人提出制定出版法的要求，陈云作出内部批示，明确拒绝了这一合理要求。陈云说：过去，我们就是利用国民党的出版法和国民党作合法斗争。现在，我们绝不允许别人也利用这样的东西和我们作合法斗争。这段批示以共产党官场上少有的坦率揭示了中共统治者的内心世界。这段批示告诉我们，其实，早在中共夺得政权之前，他们就打定主意不要自由，不要民主，不要法治。那时候，他们在表面上反对国民党的霸道，暗地里却在嘲笑国民党的愚蠢，嘲笑国民党居然也作茧自缚，在自己通过的法律面前还有所忌惮，从而给对手开展合法斗争留下一定的空间。那时候，他们在口头上反对国民党不民主，心底里却想的是，等以后我们掌了权，连这点民主也决不答应――“我们才不会象你们那幺傻呐。”<br />
<br />
　　这当然是典型的犬儒主义。还要补充一点，众所周知，陈云是中共党内有名的“老右倾”。毛泽东发动大跃进，陈云是党内高层中仅有的几个没跟着发烧起哄的人物之一，文革中更是从一开始就靠边站。可见，犬儒与否和所谓极左路线并没有什幺关系。<br />
<br />
5、从“高尚的谎言”到“赤裸的暴力”<br />
<br />
　　汉娜・阿伦特（Hannah Arendt）把极权主义的组织结构比作洋葱头，最核心是党的领袖，然后依次是领袖周围的小圈子、精英组织、党员，最外层则是党的追随者和同路人。她指出：“极权主义运动的整个等级结构，从天真的同路人到党员、精英组织、领袖周围的小圈子、领袖本人，都可以被描述为一种由轻信和犬儒等不同态度的奇特混合”，而“层次越高，犬儒态度就越是压倒轻信的情形”。也就是说，越是外层的人越是天真轻信，越是核心的人越是犬儒。这是不难理解的，譬如极权主义的欺骗性宣传，圈外人或许信以为真，圈内人既是谎言的制造者，当然不是受欺骗。问题是，圈内人为什幺会存心制造谎言？其它那些由于接近核心而了解到谎言是谎言的人如何又能心安理得地维护谎言？下面一个例子或许有助于说明这个问题。<br />
<br />
　　一九二七年春，中共按照共产国际的指示，在上海准备武装暴动，被国民党镇压而遭到失败。根据张国涛在回忆录中所讲，共产国际代表鲍罗廷与新任中共总书记瞿秋白对革命失败后的局势进行讨论商议对策。他们认为，中国革命是失败了，责任问题要有个交代，虽然是按共产国际指示行事，但不能让共产国际担负责任，因为莫斯科威信的丧失，将会影响世界革命，也会助长托派攻击斯大林的气焰，更会使中共党员不信任共产国际的领导，所以只能由中共挺身而出承担这个责任，但如果责任由中央政治局全体担负，中央的领导就会破产，所以还不如把全部责任推到陈独秀一人身上，其它人则站在拥护共产国际的英明领导的立场上反对机会主义路线，这样才能稳定中共中央的领导。<br />
<br />
　　这段故事透露了共产党领袖们所共有的一个心照不宣的秘密，在领袖们看来，要让群众坚定不移地投身革命，就必须让群众对领袖的英明正确抱有坚定不移的信心，因此就必须编造和维护领袖英明正确的神话。不错，神话就是谎言，但这是“高尚的谎言”（柏拉图语）。它是革命的需要，政治的需要。为了维护这个神话，就必须文过饰非，把一切功劳归于领袖，把一切错误推给别人，就必须压制对领袖的批评，压制自由讨论。我们知道，谎言总是有漏洞的，因此，为了防止谎言破产，就必须不断地编造新的谎言去补充，去圆谎。于是，单一的谎言就迅速发展成一套弥天大网。伴随着谎言系统的发展，在共产党内部也发展出一套强力禁制系统，一套铁血纪律。<br />
<br />
　　照理说，谎言系统与铁血纪律的形成只是为了夺取革命的胜利。不幸的是，它们并不会因为革命的胜利而自行消亡。事情往往是相反的。由于掌握了全国政权，共产党更可以把它的谎言系统和强力禁制系统扩展到整个国家，从而将之发展到更完整、更严密的地步。共产国家宣称自己是人民的国家，但是党的领袖们清楚地知道，那充其量是“为民作主”而不是“由民作主”。现在，有越来越多的材料表明，在五六年底五七年初一段时间，毛泽东看来确实考虑过是否实行真自由真民主的问题。他提出正确处理人民内部矛盾，号召“双百”方针，主张言者无罪。他甚至在和某些人个别谈话时讲到中国的问题不只是官僚主义，而且是专制主义，说现在还是在训政时期（见李慎之《大民主与小民主》）。可见在当年，虽然有许多人受惑于共产党宣传，把专制误认为民主，把不自由误认为“最大的自由”，但领袖们自己却一点不糊涂。当毛泽东发动鸣放整风时，越是圈内的人越是不当真，越是圈外的人越是当真，也就是说，越是圈内的人越犬儒，越是圈外的人越轻信。至于毛泽东本人，先前号召鸣放整风，俨然一个理想主义者，后来又发动反右运动，分明又是犬儒主义。毛泽东翻云复雨，正好说明了他是真犬儒主义假理想主义。<br />
<br />
　　专制者的犬儒主义可以有两套不同的说词，第二套比第一套更犬儒。在奥威尔（George Orwell）的小说《一九八四年》中，主人公温斯顿・史密斯是这样理解党的专制权力的，他说：“党并非为自己的目的才追求权力，而只是为了大多数人的利益。党之所以追求权力，乃因为一般群众是脆弱的、怯懦的动物，他们不配享受自由，或者说不能认识真理，必须由那些比他们强有力的人来统治他们，有秩序地欺骗他们。人类只能在自由与快乐二者之间选择其一，而对大多数人来说，快乐比自由更好些。党是弱者们的永恒的守护神，是为人民服务，它是为了善才作恶，为了他人的快乐而牺牲自己的快乐。”<br />
<br />
　　可是，身为统治精英的奥布来恩却嘲笑史密斯的这番见解。他直截了当地宣称：“党完全是为了自己才追求权力的，我们并不关心别人的利益，我们只关心权力。不为荣华富贵，不为福禄长寿，只是为了权力。……我们不同于以往任何寡头政治集团，因为我们知道我们所从事的活动是为了什幺。其它任何人，甚至那些和我们相似的人，都是胆小鬼和伪君子。他们没有勇气承认他们的动机。他们自称甚至相信他们掌权并非出于自愿，只是暂时的勉为其难；他们自称甚至相信在不远的未来就会实现自由平等的快乐天堂。我们的看法不一样。我们知道，从来没有人是为了放弃权力才掌握权力。权力不是手段而是目的。专政的目的并不是革命，革命的目的就是专政。迫害的目的就是迫害。折磨的目的就是折磨。权力的目的就是权力。”<br />
<br />
　　上述两种犬儒主义在实际上常常互相交织而难以分清。那些“为权力而权力”的犬儒主义者往往要把自己的立场加以合理化，要自己说服自己他们是在为着一个高尚的目标。许多以高尚的目标为由而把持权力的人，到头来很可能只是“为权力而权力”。譬如当今的某些中共领导人，他们一方面坚称如果没有共产党的专政就没有经济的发展和社会的稳定，而且也确实下工夫发展经济稳定社会，但另一方面又打定主意宁肯天下大乱经济崩溃也决不肯放弃一党专政。 　　<br />
<br />
6、是“从革命党向执政党转变”，还是从“权力向暴力转变”？<br />
<br />
　　越到后期，中共越是变得犬儒主义。在六四事件和苏东事件之后，中共的犬儒化是如此显着，以至于一批少壮派要忍不住将之诉诸文字，化为纲领。所谓《太子党纲领》（即《苏联剧变之后中国的现实应对与战略选择》）一文就是一个例证。<br />
<br />
　　在《太子党纲领》中，作者提出：“我们党面临的一个重要问题，是从革命党向执政党的转变。苏联事变后，这一转变更加紧迫。”初读这话，令人莫明其妙。共产党已经执政四十多年，怎幺到如今反倒提出向执政党转变的问题来了呢？若说在毛泽东时代，中共一直高举“革命”大旗，不断革命，不断搞政治运动，致使经济建设受到很大冲击；可是，这种局面不是早已随着“四人帮”的垮台，特别是在邓小平主政以来就已经结束了吗？<br />
<br />
　　认真分析那段文字，以下几句话颇堪玩味，作者批评共产党“用群众中的民粹主义情绪来束缚党的手脚”，“几十年来形成的骄民政策，对群众只敢讲廉政和勤政，不敢讲严政”；作者认为，“苏联、东欧和中国的六四事件的经验表明，政治反对派用以攻击共产党的武器，有时恰恰就是共产党尚未进行有效转变的一些革命理论”，“自由化分子最爱用的口号就是反腐败、反官倒，群众运动天然合理等……”。上述几句话，且不说有的完全不是事实，例如，共产党什幺时候实行过“骄民政策”？什幺时候“不敢讲严政”？问题是，在这些含糊暧昧的词句后面，作者到底想表达什幺意思？<br />
<br />
　　其实，《太子党纲领》的意思无非是说：过去，在理论上，共产党总是声称它的权力是来自人民群众（毛语录：“我们的权力是谁给的？人民给的”），共产党总是以人民的名义发号施令，人民变得神圣不可侵犯；因此，在过去，党从不敢正大光明地、理直气壮地压制人民。对于来自人民的反对意见，党唯一的办法是宣布对方不是人民，是“反革命”，是“一小撮”，或者是“受蒙蔽”，由此才能获得压制的合法性或正当性――这就叫“民粹主义”，这就叫“骄民政策”、“不敢讲严政”和“群众运动天然有理”。这套理论，倘放在革命时期或许有益而必要，但在执政时期就是无用而有害的了。如果继续采用这套理论，只会作茧自缚，给反对派提供思想武器。<br />
<br />
　　应该承认，上述观点确有其道理。这再次证明了共产党专制不同于传统专制。在传统专制下，统治者和被统治者是毫不含混的两件事，彼此都不认为双方是平等的。皇帝是上天的儿子，不是人民的儿子。地方官被叫做父母官，不叫“人民勤务员”。统治者固然需要体察民意，但是不一定要遵循民意，因为“民可使由之，不可使知之”，民意不是最后的依归。老百姓可以向上陈情，但决不可“作乱犯上”，人民不是国家的主人，没有什幺“主权在民”。现代共产专制则不同。起码是在名义上，共产党接过了若干民主概念。共产国家甚至比民主国家更热衷于抽象地抬高人民的地位，譬如在自己的国号上格外加上“人民”或“民主”的字样，以此表示自己取代“资产阶级民主制”的合理性与历史必然性。但这样一来，诚如《太子党纲领》所言，共产党也就使自己面临一种巨大的危险：一旦广大人民一齐起来发出呼声，拒绝承认人民政府的人民性，我们的“人民政府”就失去立足之地了。在苏东事件中，有示威者打出标语“我们才是人民！”就这样一句简单的口号，共产党政权的合法性便荡然无存。<br />
<br />
　　正是深切地感受到共产政权的这一内在弱点，《太子党纲领》建议，中共务必要改换自己的理论，放弃已经不合时宜的革命口号，要向老百姓摆明“我们统治你们”的姿态。这就是所谓“从革命党向执政党的转变”。譬如奥威尔笔下的《动物农场》，几只猪高举“一切动物都是平等的”革命大旗，带领其它动物推翻了主人琼斯的统治，建立起号称由动物们自己当家作主的新社会，尔后不久，聪明的猪们就修正了原先的革命口号：“一切动物都是平等的，但是，”猪们加上了新的半句话――“有些动物比另一些动物更平等。”这就防止了其它动物利用“一切动物都是平等的”这一过时的革命口号反过来挑战猪们的统治权。这就叫及时地完成了“从革命党向执政党的转变”。<br />
<br />
　　大家都还记得，在六四事件之后，人们普遍认定共产党已经丧失了统治的合法性。这就是说，原先共产党统治的种种理论依据，诸如“社会主义国家人民地位高”，“共产党社会是最大的民主”，“共产党代表了人类历史发展的客观规律”，如今已经彻底破产。我当然不是说，共产党统治的合法性是在六四这一天才丧失的。从理论上讲，共产党统治从来就没有获得过合法性。早在六四之前，就已经有越来越多的人认清了这一点。八九民运本身就是人民质疑共产党统治合法性的伟大壮举。我这里无非是说，六四事件毕竟是一个显着的标志，“人民的政府”公然下令“人民的军队”屠杀人民。这不能不使得一切为中共统治合法性进行辩护的说词彻底破产。然而现在的问题是，这样一个丧失了合法性的统治并没有随之垮台。它依然立在那里。这又是怎幺一回事呢？<br />
<br />
　　有人解释说，六四之后，中共继续开展经济改革并取得了可观的成就，这就为中共统治赢回了合法性。另有一种解释说，今日中共扮演了国家利益或民族利益的保护者的角色，因而其统治便具有了合法性。这种解释误解了政权合法性的意义。一个政权做出了某种政绩，这是一回事。一个政权具有合法性，这是另外一回事。如白鲁洵所言：“没有一个政权愚蠢到把成功的政策作为其统治合法性基础，因为合法性的本质就在于它不管实行了怎样有偏差的政策而仍旧承认这个政权。”好比婚姻的合法性，如果不经女方同意（在古代则需经父母同意）并办理结婚手续，单单是因为男方对女方做出了一个合法的丈夫所应该作出的某些事情，那决不等于双方就具有了合法的婚姻关系。否则，强奸、强占和婚姻还有什幺区别。反过来，一对合法的夫妻，即便一方或双方出于某种原因未能很好地完成丈夫或妻子的应尽之责，只要未经过必要的程序解除婚约，该婚姻的合法性就依然存在，也就是说，丈夫依然要承认妻子是妻子，妻子依然要承认丈夫是丈夫。这一点放在任何时代都不例外。<br />
<br />
　　今天，中共已经失去了统治的合法性。它之所以还在统治仅仅是因为它掌握着统治的权力，此外再无其它理由。权力一词已经用得很滥很泛。人们常常把任何具有强制性的力量都称作权力，这就和暴力一词分不清界限了。严格说来，权力是和暴力不同的。强盗拿着手枪要我交出钱来，这是一种情况。政府派人向我收税，这是另一种情况。你或许可以说在这两种情况下，我交钱都是出于被强制。如果我不交，强盗会开枪打我，政府则会抓我坐班房。然而这两种情况分明又很不一样，因为我承认我向政府交税是应该的，我承认政府有权要我交税，但强盗并没有类似的权利。可见，政府的强制力才叫权力，强盗的强制力只能叫暴力。概括言之，凡是得到被强制者承认其正当性的力量叫权力，凡是不被我们认可的强制力叫暴力。罗素说得好：“假如一种权力完全因为它是权力而受人尊重，并无其它任何原因，这种权力就是暴力。因此，一旦传统不再为人所承认的时候，原先的传统权力就变成暴力。”<br />
<br />
　　现在，我们总算懂得《太子党纲领》的真正含义了。声称要完成“从革命党向执政党转变”，其实就是承认和宣布从权力向暴力转变。照纲领的作者们看来，共产党统治的合法性外衣横竖已经百孔千疮，现在索性打开天窗说亮话，干脆摆明了我们（中共）就是靠暴力实行统治。暴力自有暴力的好处，失去了意识形态的自我粉饰，同时也就免去了意识形态的作茧自缚，从此更可以方便行事。走到这一步，共产党的犬儒主义也就走到了它的极点。<br />
<br />
7、为什幺“不改革等死，改革找死”？<br />
<br />
　　苏东事变后，有人讲过这样一句俏皮话：“不改革等死，改革找死。”这话的前半句容易理解，后半句则令人深思。为什幺那些主动进行改革的共产国家非但没有延长其一党专政的寿命，反倒加速了它的灭亡呢？<br />
<br />
　　大致上讲有两条原因，一条是一般性的，一条是特殊性的。一般性原因即所谓“期望提升律”（Law of expectations），既得陇，又望蜀。人们越是在某些领域赢得了自由，他们就越是对其它领域还存在的不自由感到难以忍受，他们就越是要求赢得更多的自由。在统治者方面，诚如托克维尔所言，专制统治最脆弱的时候，常常不是其政绩最恶劣的时候，而是它开始改革，从而使其弊端公开显现暴露的时候。特殊性原因在于，共产党的改革，即便在初期只限于经济领域，也具有自挖墙脚、自掘坟墓的意义。因为共产党的经济改革是改掉社会主义，引进资本主义。它的改革不是自我完善而是自我否定。过去，共产党压制自由民主，唯一的法宝就是给对方扣上“资产阶级”、“走资本主义道路”的罪名，一旦共产党自己就在心照不宣、路人皆知地带头走资本主义道路，带头当资产阶级，那幺，它还有什幺理由再去坚持所谓“无产阶级专政”，去压制所谓“资产阶级”自由化呢？因此，随着改革的日益深入，共产党内部的分化也就日益显着。那些正直的共产党人，既然他们在经济上主张改革，赞成私有制市场经济，他们在政治上也就顺理成章地主张开放，认同自由民主制度。这样，一方面，在民间，有越来越大的要求自由民主的压力；另一方面，在党内，有越来越多的人同情和支持社会上的自由化运动，至少也是出于理亏心虚而不肯严厉地压制自由化运动。两者交互作用，彼此强化，于是就加速了共产专制的败亡。<br />
<br />
　　我们知道，在改革初期，在中共党内，经济上的改革派往往在政治上是温和派。反过来，政治上的强硬派也往往是经济上的保守派。其实，党内保守派之所以反对经济改革，主要不是出于经济上的理由而是出于政治上的考虑。他们担心经济改革会瓦解共产党统治的意识形态根基，从而直接危及共产党的专制权力本身。据说，当年蛇口经济特区搞得有声有色，颇引起一班“老一代无产阶级革命家”忧心忡忡。他们问：“难道这还能叫社会主义吗 ？”有人回答得很妙：“只要是共产党在掌权，就叫社会主义。”于是，这些“老一代无产阶级革命家”也就放心了。<br />
<br />
　　由此，我们可以澄清一种流行的谬误。许多人把邓小平路线称为实用主义，应该说邓小平路线不是实用主义而是犬儒主义。因为实用主义强调理论与实践相一致，邓小平路线的特点恰恰在于理论与实践相背离。正象近年来中共干部中流行的一段顺口溜――“说社会主义，干社会主义，是极左派。说资本主义，干资本主义，是自由化派。说社会主义，干资本主义，是邓小平派。”时至今日，邓小平派的“说社会主义”的欺骗性早已被世人看穿。照理说，谎言一经识破就不再具有效力，但奇怪的是，邓小平派的“说社会主义”这套谎言似乎依然很有效，中共依然可以在“说社会主义”的名义下去压制所谓“资产阶级”自由化思潮。其间原因何在？原因就在于普遍流行的犬儒心态。汉娜・阿伦特发现，“极权制度的巧妙之处恰恰在于它取消了要幺揭露谎言、要幺迫使领袖言行一致的现实”。因为“广大党员和精英集团的犬儒态度解除了领袖被迫面临的压力”。这些人非但不去揭露谎言，不去以子之矛，攻子之盾，从而促成权力的性质转换，相反，他们对这种挂狗头卖羊肉的做法大表佩服，对权力堕落为赤裸裸的暴力的现状大表赞赏，以为那是高明，是老辣，是有政治智能。可见犬儒病已经深入到何等程度。<br />
<br />
　　罗素指出：“当维持传统权力的信仰和习惯趋于失势的时候，不是渐渐地为基于某种新信仰的权力所取代，就是为暴力所取代。”共产国家推行改革，其结果是导致了原有的意识形态即传统信仰的破产。在苏东各国，专制权力转化为民主权力。八九民运功亏一篑，使中共暂时得以幸免“改革找死”这一劫。六四屠杀彻底消解了共产党统治的合法性，促成了中共政权由权力向暴力的转变。九二年邓小平南巡讲话，公开声称不问姓社姓资。至此，共产党不再担心别人以子之矛，攻子之盾，因为它已经不再需要意识形态的乔装打扮。现在，维持暴力统治的手段只剩下了暴力自身，而协同这种统治的心态也只剩下了犬儒主义。<br />
<br />
8、犬儒心态与恐惧，兼论“难得糊涂”<br />
<br />
如果说共产党犬儒化的根源是“为权力而权力”，那幺，民众犬儒化的 根源便是恐惧。<br />
<br />
　　共产专制是谎言加暴力，与此相对应，其治下的人民则是轻信加恐惧。严格说来，轻信和恐惧是互相矛盾的。当我们把谎言误认作真理的时候，我们同时也就把暴力误认作正义的力量，误认作人民自己的力量，我们就不会对之感到恐惧。如果我们在“无产阶级专政”面前感到恐惧，那就说明我们将它视为异己之物，我们其实并没有把它当作自己的力量。然而在现实生活中，一般人的确是既轻信又恐惧，同时兼有这两种互相矛盾的东西。这就是奥威尔所说的“双重思想”。在这里，轻信是表层意识，恐惧则存在于下意识。<br />
<br />
　　不妨举个小例子。在过去，假如在我们的亲友熟人中有一个被戴上帽子的右派分子，通常我们都会小心翼翼地避免和他深入接触。我们很少会认真询问他是怎幺被打成右派的。凭着直觉，我们其实不大相信他真是“敌人”，但是我们并不去努力了解事实的真相。这表明我们下意识地害怕了解事实真相，因为我们担心真相会动摇我们对党的信仰。有时，我们分明知道别人是被冤枉的，但是我们并不为他公开地鸣冤叫屈，因为我们害怕被株连。这表明我们从骨子里并不相信我们的制度是公正的制度，是讲理的制度，是保护好人的制度。这就是说，即便在我们自以为对共产制度最为信仰的时候，我们的信仰其实也是很表面化的，在信仰的外壳下包藏着的是恐惧。轻信和恐惧之所以能够在同一个人的头脑中共存，无非是因为我们平时总是把恐惧赶到地下室，避免让它直接与轻信面对而已。<br />
<br />
　　这已经是犬儒主义：我们下意识地不相信我们上意识所相信的东西。如果说统治者的犬儒主义表现在他们其实并不相信他们口头上标榜的原则，而只是把那些原则当做维护权力的手段；那幺，民众方面的犬儒主义就表现在我们也并不信仰我们声称的原则，而只是以此来掩饰我们的恐惧，为我们屈从权势加以合理化。因此，毫不奇怪，随着阅历的增长，随着极权统治的谎言的日益暴露，许多人并不是奋起反抗，可见他们原本就不是真诚的受骗者 ，他们先前的轻信其实并不纯粹是轻信。许多人只会变得更消极，更世故，更小心翼翼地不越雷池一步，甚至努力去适应这个谎言的世界。如果不是在轻信减少的同时恐惧却在增加，人们又何至于此呢？<br />
<br />
　　一般人常常高估了共产党意识形态的欺骗作用。他们总以为人们没有起来反对专制，仅仅是因为他们没有识破谎言，没有认识到专制是专制。其实不尽然。有时情况刚好是反过来的。正因为他们多少意识到谎言是谎言，感觉到专制是专制，若反抗必将招致镇压，所以才出于恐惧而不敢抗争。以文革为例，文化革命既是极权统治的登峰造极，又是极权统治空前的自我暴露 。现在，许多人只是片面地强调文革的解放思想的作用――文革确实促成了广大民众识破专制制度的谎言并造就了一小批真正的反叛者，但忽略了它的恐惧效应――文革同时把多数人变成了逃避政治的逍遥派。作为民主墙运动一份子，我当时就注意到，运动的参加者人数很少，年龄较轻，多数人的思想见解还谈不上透彻，有些人显然还对当局抱有若干幻想也就是还比较轻信。相比之下，我过去结识的另一批朋友对中共的看法，包括对邓小平的看法则要深刻得多，尖锐得多。可惜的是，后一种人往往不会参加到运动中去，他们只是在远处予以关切，以悲观的态度而乐观其成。在八零年的北大竞选运动中也出现类似的对比，偏偏是年纪轻、阅历少，并不以为当局是那幺专制的本科生们要比研究生们更投入。至于说在五七年的鸣放运动中，尤以青年知识分子的“右派”言论最为大量也更其尖锐，这在很大程度上竟是因为他们中了毛泽东的“阳谋”，也就是对共产党更轻信而造成的。事后我们不难指出，八九民运的若干学生领袖因为对共产专制的凶残本性认识不清而未能避免六四屠杀（这话其实只有片面的真理，按下不表），可是反过来想一想，要是他们都是从一开始就充分认识到当局的凶残，那幺，还会有多少人敢于发起这场运动，投入这场运动呢？许多成年人不正是因为对当局的凶残看得太清楚，才置身事外，才去劝阻学生们不要上街的吗？<br />
<br />
　　恐惧并非犬儒。它只是通向犬儒的桥梁。在极权社会中，恐惧感深入人心，然而问题又在于，如果人们能够直面正视自己的恐惧，那倒好了。因为怕和恨是同一事物的两面，怕就是恨。承认恐惧，认识恐惧，必将把我们推向异议者、反对者的立场，哪怕我们只把异议隐藏在我们的内心。恰恰因为正视恐惧必然会刺激良知，会导致良知的觉醒与反叛，多数人才会有意无意地回避恐惧，掉过脸去假装看不见，假装没有这回事，宁肯让良知处于糊涂状态。这就叫“难得糊涂”。据说在六四之后，郑板桥的“难得糊涂”横幅小小流行了一阵。这应该不是偶然。这样的“难得糊涂”就是犬儒主义。由聪明变糊涂才叫“难得糊涂”。“难得糊涂”是聪明的不聪明，是不糊涂的糊涂；但是，“难得糊涂”终究是糊涂，是不聪明。只有肯思考的人才会变成犬儒，而一旦变成犬儒你就可以不再思考。只有聪明人才需要变糊涂，这要求你必须给自己的聪明套上笼头，不让它去深入思考那些容易惹麻烦的现实问题。日子一长，你的聪明会退化，于是，你就终于修练到难得的糊涂境界了。“难得糊涂”式的犬儒主义无异于精神的自我阉割。它是对理智的背叛，对聪明的背叛。<br />
<br />
9、从人文精神讨论谈起<br />
<br />
前几年，国内知识界兴起了一场有关人文精神的大讨论。人文精神问题的提出，是因为一些年轻的知识分子痛感于人文精神的失落：商业大潮的冲击，大众文化的勃起，使人文知识分子顿时间失去了位置，失去了影响，失去了尊重，甚至失去了自己的语言。此论一出，在知识界立即引起相当的共鸣。也有人提出异议，他们反问，人文精神难道在过去果真高扬过吗？市场经济的发展，意识形态的淡出，社会生活包括文化本身的的多元化，难道不是我们所追求的目标，为什幺当它们开始成为现实时我们又要拒绝，又要抱怨呢？在世俗化的现代社会，知识分子本来就不处于中心位置，因此，知识分子不应该再怀抱精英心态，再幻想着当时代英雄，而必须安于边缘，甘于本份。另有一些论者则从不同的角度对这场讨论发表评论。他们指出，人文精神的倡导者往往把矛头指向世俗化、商业化或大众文化，有意无意地回避了专制主义这一政治现实。更有论者提醒人们，要注意到这场讨论的形而下方面，尖锐地批评了“嘲笑道德嘲笑理想嘲笑精神”的虚无主义、犬儒主义。<br />
<br />
　　人文精神讨论涉及到许多问题。这里，我只打算就其中几个问题略加评论。<br />
<br />
　　首先我要指出，产生于九十年代的人文精神失落感，其参照系乃是八十年代（确切地说，是从七十年代末到八九这段时期）。事后回顾不难发现，八十年代不愧为自四九年以来中国大陆人文精神最为高扬的时期。正是在这段时期，大量的人文知识分子成为时代英雄。这不足为奇。我们知道，共产国家是意识形态统治的国家。共产党以一种包罗万象的意识形态对整个社会进行全面彻底的控制。在这里，每一种控制和压迫都假借理论的名义，因此 ，每一种挑战和反抗也不得不首先是理论的挑战和反抗。如果我们把批判精神定义为知识分子的基本特征，那幺，他的黄金时代就正该是意识形态统治社会的后期，知识分子批判作为专制基础的意识形态，他瓦解了那套意识形态，从而也就瓦解了专制统治（说明：这里所说的知识分子，不是指身分或职业，而是指社会角色。故而包括了那些未受过高等教育也不是从事脑力工作的持不同政见者）。在八十年代席卷整个共产世界的民主大潮中，知识分子扮演了先锋和领袖的角色。知识分子在这一时期所发挥的重大作用，恐怕只有西方历史上的启蒙时代可相比拟。<br />
<br />
　　正象医生的重要性在于使自己的存在变得不重要，同样地，以消解意识形态魔性为己任的知识分子，其重要性也不可避免地会随着其使命的完成而下降。譬如在八十年代末九十年代初的前苏东各国，知识分子很是风光了一阵。作家当上总统，诗人成了部长，教授被选为议员，在国会大厦里充斥着还没毕业的大学生，严肃的政治性书刊风行一时。这在当年都是很普遍的现象。不过这种情景通常不会持续得太久。这也难怪，既然知识分子所批判的专制怪物已然寿终正寝，他们倡导的基本原则已然被公众接纳并铸入体制，他们的历史作用也就告一段落。例如此前饱经磨难的异议人士，他们顶上的道义光环每每会随着政治迫害的终结和政治悲情的消退而逐渐失色。当民主从理念变为运作，行动人脱颖而出，理念人则可能脱离中心而成为背景。好象看电影，观众注目的是演员，编剧则被忽视。民主政治是凡人政治或曰庸人政治，曲高和寡在所难免。所以，许多最早鼓吹自由民主理念的人，待到自由民主实行后倒很容易感觉落寞。这里还不说商业化与大众文化的影响。<br />
<br />
　　那幺，中国的情况又是如何呢？应该说，中国的人文知识分子在九十年代的失落感实际上包含两个方面的问题。一个方面的问题与上面所说的情况类似。在八十年代，一部在纯学术纯艺术领域中突破原意识形态框架的作品都可以在学术圈艺术圈之外的广大公众中引起轰动，这在九十年代就几乎不可能，因为原意识形态已经从这些领域中溃败撤离了。在这里，知识分子是因为他们的胜利而落寞。另一个问题则全然不同。在中国，政治专制依然存在，这个被八十年代知识分子批判的主要目标依然存在。照理说，知识分子的英雄戏还没唱完，怎幺就被抛向边缘了呢？当然，这是因为政治专制政治高压。在中国，人文精神的高扬期与失落期是以六四分界的，这本身就给出了答案的线索。<br />
<br />
　　然而，答案的线索还不是答案。不错，六四事件压下了一批知识精英，但是，知识界的批判并未因此偃旗息鼓。仔细检索九十年代大陆的公开出版物可以发现，宣扬自由化的文字未必少于八十年代，其深度力度未必不如先前，甚至多有过之。异议人士的批判抗议之声连绵不断，而且比六四前更加鲜明。老百姓的“什幺话都敢说”的情况并没有衰减，而且更尖锐、更普遍――如今，连不少共产党的干部也都在指名道姓地批评共产党了。甚至当局对思想的控制似乎也比八十年代更弱而不是更强。许多在八十年代遭禁挨批的观念，到了九十年代反而显得稀松平常，通行无阻（例如人道主义的观念）。现在，不少知识分子和异议人士的感慨是：并非我们不敢批判，并非当局压制得太严，问题是如今的听众数量较六四前大为减少，我们的呼声缺少广泛的回应，别人都忙着下海挣钱奔小康去了。无怪乎一批论者要把矛头指向商业化世俗化及大众文化了。<br />
<br />
　　我并不否认经济大潮的冲击。九二年邓小平南巡讲话发起了新一轮经改热潮，这确实转移和减轻了政治专制所面对的压力。但我要指出的是，导致自由呼声缺少回应的更重要的原因是泛滥成灾的虚无主义和犬儒心态。这是六四事件留下的最恶劣的后遗症。<br />
<br />
10、自由民主理念――信则灵，不信则不灵<br />
<br />
八九民运是一场伟大的的理性抗争。在八九民运遭到暴力镇压之后，一些学生忿忿地说：“你们说不过我们，我们打不过你们。”理性胜了――有理说赢了无理；但是，理性又败了――理性输给了暴力。在八九民运中，示威者赢得了极为广泛而热烈的支持，二十万大军兵临城下，硬是让自发组织起来的百万民众生生地挡在外面开不进来。这时候，人们深信理念是有力量的，理性是有力量的，思想能够瓦解刀枪，征服刀枪。然而，六四屠杀残酷地镇压下了民众的抗议。六四屠杀的直接后果是激起了人们更大的愤慨。“失人心者失天下”，人们相信这样一个杀人的政权绝不可能维持长久。而后紧接着发生的苏东巨变进一步加强了人们的这一信念。可是，几年过去了，好象什幺事情都没有发生。中共政权没有垮台，它依然立在那里。这不能不使许多人感到沮丧，感到失望。这就引出了六四的另一个后果：许多人因此而失去了对理念、对理性的信仰。不管我们如何反复论证说明六四的结局绝非必然，以期在人们心中重建对理性抗争的信仰，可惜的是，多数人一时间不会这幺想，他们更容易从那一震撼中得出一个简单化的结论：思想打不过刀枪，理性敌不过暴力。<br />
<br />
　　什幺是信仰？信仰不只是认知，信仰更是意志。信仰某种理念不仅意味着你知道那种理念是好的，是对的，而且还意味着你要身体力行，你要兑现这种理念。理念、理性之所以有力量，就在于人们信仰它实行它，否则就没有力量。自由民主这套理念，信则灵，不信则不灵。现在的问题正在于，虽然从认知的角度，许多人仍然认为自由民主是好东西，但是他们不再信仰，不再参与，不再身体力行。他们陷入了犬儒式的悲观主义，一种循环论证的自我实现的悲观主义：因为他们不相信自由的理想可以实现，所以他们不去为理想而努力，所以理想当然就不会实现，所以他们就更不相信，所以就更不努力，所以就更不相信，所以……所以……。他们知道现实是丑恶的，但因为现实是现实，他们不认为他们能够改变，所以还是接受了现实。他们知道理想是美好的，但因为理想只是理想，理想在现实中不存在，行不通，总是碰壁，所以放弃了理想。一旦人们放弃了理想，他们也就不愿意再去倾听理想的呼声。他们觉得那都是空谈，毫无用处，只是徒增烦恼而已。一位在大学教书的朋友告诉我，过去，他在讲坛上阐述自由理想，堂堂暴满；现在他在课堂上讲同样的内容，却听者寥寥。“现在的年轻人太实际了”，他说。“不，”我说，“是现在的年轻人太犬儒了。”年轻人变成犬儒实在是最可悲之事，他们还不曾追求，就已然放弃；他们还不曾长成，就已经衰老；他们还什幺都不知道呢，就已经什幺都不相信了。<br />
<br />
　　我们讲启蒙，告诉人们什幺是善什幺是恶。这里暗含的前提是，一旦人们认识到善，他们就会从善，认识到恶就会抗恶。但是启蒙对犬儒们似乎是没有意义的。犬儒是不分善恶的，但他不是不知善恶。犬儒是知善而不善善，知恶而不恶恶。有些犬儒还走得更远。他们不但不反对恶，他们还要迎合恶，只要恶有权有势。他们变得很势利。其实犬儒本来未必全是势利，但是既然他抹杀了善恶是非，结果到头来只剩下了势利。有的犬儒富于进攻性。他们不只放弃理想，还要反过来嘲笑理想。说来也是，给别人奋斗泼冷水的人，往往不是从没奋斗过的人，而是那些自己也曾奋斗过、但遭受挫败后放弃了奋斗的人。吃不着葡萄就说葡萄酸，自己不再奋斗了，看见别人还在奋斗就去嘲笑。这多半是以攻为守，嘲笑别人的奋斗是为了辩护自己的不奋斗。犬儒的嘲笑也有他自己的逻辑。犬儒既然不承认理想只承认现实，他就会把别人不顾在现实中碰壁而执意追求理想视为“傻冒”。尤其是在现今的中国：过去，共产党实行“全面专政”，“三面架机枪，只准走一方”，给个人留下的出路太窄；如今却是“一面架机枪，可以走三方”，只要不挑战政治上的专制权力，干什幺都行。你偏偏要朝枪口上撞，不是“傻冒”是什幺？人生苦短，大家都受了半辈子穷，现在好不容易有了大发其财的机会，为什幺非要争什幺自由民主不可呢？<br />
<br />
　　顺便反驳一种说法。不少人认为，现今中国大陆的民运难以开展，是因为经济改革取得实效，民众富了起来，物质主义泛滥，冲淡了政治上的追求，知识分子也获得了更多的空间和相对的自由，所以对现状也比较满意。此论似是而非。回想八九民运爆发之前，经济改革不是也搞得不错吗？那时还是经改的蜜月期，人人有糖吃，无非有多有少，还不象现在，上千万工人下岗，几家欢乐几家愁。至于知识分子的处境，那时正是高峰期，自我感觉普遍良好。八九民运的爆发，在民众方面来说，如第7节所言，主要是得陇望蜀期望提升的结果，而不是绝对剥夺相对剥夺的产物。这反过来也就说明了，现在民运的低落，是因为政治理想政治热情遭受严厉打压被冷冻被放弃，不是因为人们利益的满足而是因为期望的收缩，不是因为物质主义的泛滥而是因为犬儒主义的蔓延。<br />
<br />
11、玩主式的犬儒主义<br />
<br />
　　密尔说专制使人冷嘲，鲁迅说，在中国，专制使人麻木。冷嘲大概总比麻木好。可见我们也在进步。犬儒一词，许多人感到陌生，若说起“玩主”，大家就熟悉了。其实，玩主就是犬儒。<br />
<br />
　　前些年，以王朔作品为代表的调侃文学风靡一时。我们知道，作家大于作品，作品大于观念，对作品的诠释又大于作品本身。这里，我们不讲王朔，也不讲王朔的作品，只集中讲王朔作品的走红这一社会文化现象。学者专家已经发表过许多意见，其中不乏破的之语。有学者指出，王朔作品在多年以前就已经出现，但是作为一种玩主思潮在北京走俏，有特定的背景，实际上反映了九十年代初弥漫于整个社会上下的玩世不恭的虚无感。有人提到了“弥漫社会底层的无可奈何的失败主义情绪”。这就点到了六四。很明显，犬儒主义是在理想主义惨糟失败后发生的。玩世不恭的心态是在严肃的追求横糟蹂躏后发生的。巨大的精神压力会使人深感疲惫，摆脱疲惫的最简单的办法是扔掉精神再付之一笑。为什幺要“躲避崇高”、“渴望堕落”呢？因为“只要你自己不把自己当人看，就没有人把你当人看，你就痛快了，世上没有翻不过去的坎”（王朔小说语）。阿Q每遇挫败，要幺精神胜利，要幺自轻自贱，两法都有解脱之效。其实，不只是阿Q，就连一些执着的理想家在碰壁之后也常常要自嘲一番的，自嘲就是自疗。若置身于旁观者的地位，要嘲笑别人的碰壁就更容易。因为庄严与可笑本来就只有一步之差。<br />
<br />
　　玩主多刺。但刺有两种，“蜜蜂的刺，一用即丧失它自己的生命；犬儒的刺，一用则苟延了他自己的生命”。因为犬儒之刺，读后“只使读者觉得一切世事，一无足取，也一无可为”（鲁迅语）。玩主的冷嘲热讽，是连澡盆带孩子。它使官方的说教显得可笑，同时也使严肃的抗争显得可笑。但是权势是不在乎你嘲笑的。这话看来费解：如果有了“笑骂由人笑骂”，这不是有了舆论监督了吗，怎幺还能“好官我自为之”呢？罗素说：“自由思想与强烈批评的时期容易发展为暴力时期。”这话也费解。其实两句话都涉及到一个同样的背景，那就是犬儒主义流行病。当权力遭到自由批评从而失去传统信仰的支持，如果它不是转化为基于新信仰的权力，就变成赤裸裸的暴力。暴力统治意味着民众在政治上的消极冷漠，意味着普遍的犬儒。在暴力统治下，即便有自由批评也不足以形成与论，即便有舆论也不足以形成政治力量。所以暴力不在乎有人批评，因为它本来就是强加于人而并不需要人们的认同。你骂你的，我干我的，你能奈我何？另一方面，澡盆经摔而孩子不经摔。犬儒嘲笑权势，于权势无甚大碍，犬儒嘲笑抗争，却对抗争有大伤害，因为抗争的力量端在于道义，端在于赢得人们的支持同情。<br />
<br />
　　犬儒揭穿中共的真面目，但那不等于犬儒要成心和中共过不去，因为犬儒认为换上别人也不会有什幺差别。犬儒对民运同样不以为然。他认为民运无非是想取而代之。当中共镇压民运时，犬儒的态度是：“都不是什幺好东西，我谁也不帮。”这其实是答非所问。因为我们并不是要求你反过来帮助民运镇压中共。我们只要求你说个公道，谁也不准镇压谁。争自由争民主不是打天下坐天下，犬儒总是有意无意地把两者混为一谈。<br />
<br />
12、避世型犬儒与愤世型犬儒<br />
<br />
　　当今中国，还有两种犬儒主义，一是避世型，一是愤世型。专制之下有避世心态实不为怪。所可怪者，是今天的避世者每每以自由派自居，甚而以自由主义相标榜。<br />
<br />
　　九零年底，我写了一篇提为《自由主义思潮在中国之命运》的长文，其中论及伯林（Isaiah Berlin）提出的消极自由与积极自由两种概念，后来有位美国朋友试图把他译成英文。我读英文译稿，发现译者――看来他不熟悉伯林的哲学――把消极自由（negative liberty）和积极自由（positive liberty）译回英文时，译成了passive liberty和active liberty。这一误译使我想到，也许，把negative liberty和positive liberty译成否定性自由与肯定性自由更为妥当，因为“消极”一词容易使人联想到“被动”的意思上去。近几年来，国内学人也对两种自由概念发生了兴趣。果然有一些人望文生意，把消极自由当成了躲避现实的同义词（这是否与道家传统有关？）。以前读毛泽东的《反对自由主义》，见他把什幺“事不关己，高高挂起，明知不对，少说为佳，明哲保身，但求无过”都当成了自由主义，只觉得荒唐可笑，殊不知还真有人去认同这种毛氏的“自由主义”。譬如，有人主张“撤离广场，退回书斋”，有人要求“历史的缺席权”；不少人对现在流行的政治冷感大加称赞，主动提倡老百姓“莫谈国事”；有人说，既然自由主义认为管得罪少的政府就是最好的政府，那幺，人民最不关心的政治就是最好的政治。上述种种观点都出自所谓自由派知识分子之口，都冠以自由主义之称。这哪里是自由主义？这是遁世主义，是犬儒主义。<br />
<br />
　　愤世型犬儒看上去不象犬儒。愤世嫉俗者是孤高自傲的理想主义者，他们自己坚持理想，只是觉得世人都太俗太劣，要和世人打交道获得胜利，你只能“以其人之道还治其人之身”。也就是说，愤世嫉俗者认为，要使理想的目的取得成功，你不得不采取很多不理想的手段，这就有可能滑向犬儒了。譬如，理想主义者鲁迅讲过：“前年，我作了一篇短文，主张落水狗还是非打不可，就有老实人以为苛酷，太欠大度和宽容；况且我以此施之人，人又以报诸我，报施将永无了解的时候。但是，外国我不知，在中国，历来的胜利者，有谁不苛酷的呢。取近例，则如清初的几个皇帝，民国二年后的袁世凯，对于异己者何尝不赶尽杀绝。只是他嘴上却说着什幺大度和宽容，还有什幺慈悲和仁厚；也并不象列宁似的简单明了，列宁究竟是俄国人，怎幺想便怎幺说，比我们中国人直爽的多了。但便是在中国，在事实上，到现在为止，凡有大度，宽容，慈悲，仁厚等等美名，也大抵是名实并用者失败，只用其名者成功的。然而竟瞒过了一大群傻子，还会相信他。”<br />
<br />
　　从这段话，我们多少可以看出鲁迅在晚年为什幺会同情共产党，可以看出毛泽东为什幺会把鲁迅谬比知音。我们还可以看出鲁迅的某些观点（如“痛打落水狗”的观点）为什幺会在文革中被利用滥用，起到很恶劣的效果。这就是鲁迅思想中暗含的犬儒主义。这种犬儒主义不相信宽容仁慈一类政治美德，准确地说，那是由于痛感到宽容仁慈一类美德在现实政治中是那样的无能、无力，故而在愤激之下将之抛弃，走向犬儒。<br />
<br />
　　这又涉及到信仰的问题。信仰还有一层意思。当我说我相信理性时，那不仅意味着我自己有理性，我自己要遵循理性行事，那还意味着我相信别人也有理性，别人也会遵循理性，那还意味着，我认为合理的别人也会认为合理，这就叫“人同此心，事同此理”。八九民运中，挡军车的市民们何尝不知道肉体抵不过枪炮，他们无非是相信解放军也是人，也有良心也明事理而已。所谓六四事件摧毁了人们对理性的信仰，那首先是让许多善良者失去了对他人的信任，进而是让一些坚持抗争者失去了对一般民众的信仰。由此产生了对理性抗争方式的怀疑。于是，有人重新提出了暴力抗争的主张。当然，以暴抗暴未必总是导致以暴易暴，正如暴力主张未必就是犬儒主义。不过，在这两者之间确实可能有着某种关联。如第4节所讲，革命者决心诉诸暴力，那不仅表明他们对统治者不抱幻想，同时也表明他们对人民深感失望。这就很难和他们声称的民主理想相一致。这里就暗含着犬儒主义的因子。<br />
<br />
13、理论的犬儒化<br />
<br />
　　进入九十年代以来，由于官方意识形态的破产，各种非官方思潮应运而生。按照陈奎德在《迎接新诸子时代》一文中的归纳，当今中国，较有影响的政治思潮即有十四种之多，它们包括民主主义、民主社会主义、自由主义宪政派、经李泽厚解释过的“经典马克思主义”、崔之元等的“第三条道路”、新马克思主义、新儒家、基督教民主主义、三民主义、新保守主义、太子党纲领的“国家主义”、强权政治现实主义、邓力群等的毛主义以及反西方主义的极端民族主义。上述各种思潮，大多数已经脱离了、至少是部份地脱离了官方意识形态的轨道。有的虽然沿用了官方意识形态的概念和框架，但其内容却和官方不尽一致，如李泽厚式的“经典马克思主义”和新马克思主义；有的虽然是为中共一党专政之现状作辩护，如新保守主义，但却完全抛弃了马列那一套术语，转而从柏克、严复和哈耶克一类所谓“资产阶级”学者那里寻找理论依据。<br />
<br />
　　值得注意的是，以上列举的多数思潮都并不否定自由、民主、人权、法治等基本价值。譬如言论自由原则，如今已很少有人公开反对。所谓“不给反革命言论以言论自由”这套谬论早就被驳得体无完肤，甚至连中共当局也不得不从刑法中删除了“反革命罪”。照理说，现在既然有如此众多的思潮在言论自由问题上达到共识，因而它们可以、也应该汇集为一股要求言论自由、反对因言治罪的强大压力，然而事实却并非如此。翻阅新诸子的大作，我们发现，许多论者对言论自由一事要幺只字不提，要幺虚晃一枪，轻轻带过。这不能不使人怀疑他们对言论自由原则以至对各自主张的学说究竟有没有严肃的承诺，到底当不当真。<br />
<br />
　　我们知道，中共官方的马克思主义一向是犬儒式的马克思主义，它只利用马克思主义而不忠于马克思主义。官方从来只在马克思主义中摘取和夸张对自己有利的部份，回避和抹杀对自己不利的部份。如今知识界流行的种种“主义”，不少也犯有同样的毛病。某些论者根本不管理论的完整性或逻辑性，只捡掌权者爱听的话说。例如有人大声疾呼要强化国家能力，这主张孤立的看也无大错，因为今天中国政府的问题，一方面是管了不该管的，另一方面是该管的没管了，可是这批进言者只讲后一方面不讲前一方面，只讲政府要有效不讲政府要有限。新保守主义者频频引用柏克、严复的言论，但只引用他们维护权威的言论，不引用他们维护自由的言论。极端民族主义派只抨击来自外国的自由民主理念，不批评同样是来自外国的马克思主义和一党专政。<br />
<br />
　　说到中国知识界的犬儒病，有些后现代派可谓突出一例。乍一看去很奇怪，后现代派在西方本属激进派，引进中国却成了保守派的同盟军。这当然和后现代派本身有关。粗略地说，后现代理论和马克思理论相似，它们都是源于西方文明而又反对西方主流文明，因此不难被借来作抵制西方自由民主理念之用。再说，把西方的后现代理论硬搬到仍处于前现代的中国，也很容易导致桔逾淮而为枳的荒诞效应。更重要的是，中国的后现代派（当然不是全部）和西方的后现代派其实不是一回事，后者是理想主义，前者却是犬儒主义。中国的后现代派热衷于揭露西方自由民主的虚伪性，它并不是只批判西方社会的现实，它连带着也要否定自由民主的理想，一旦理想被否定，再坏的现实也就得到了原谅。中国某些后现代派的逻辑是，既然西方社会也没有真正的自由民主，所以我们中国没有自由民主也就不足为病；既然天下乌鸦一般黑，充其量不过是五十步笑百步，那幺，我们又何必非要对中共这只黑乌鸦过不去呢？<br />
<br />
　　以上，我对知识界的犬儒病作了一番剖析。这并不等于说唯有知识分子才害了犬儒病，也不等于说别人的犬儒病全是让知识分子给传染的。犬儒病是整个社会的流行病，知识分子不过是患者之一，其症状也未必此别人更严重。只因为知识分子要发议论，写文章，从而留下了证词，因此为我们分析批评提供了素材。正因为他们的这些议论反映了社会上的普遍心态，所以才值得我们格外重视。<br />
<br />
14、关于假装服从的政治游戏<br />
<br />
　　据说，近几年知识界有人提出一个口号，叫“背对当局”（这使我想起七十年代初期成都人流行的一句话：“理他算输”）。这当然是号召人们抵制专制政权，不过是用消极的方法抵制。应该承认，今日中共专制统治已较先前大为松懈。政府表面上号令天下，什幺都管，但事实上，下面的人却往往另搞一套，遇到红灯绕着走，上有政策，下有对策，下面只要不公开挑战，政府也就眼睁眼闭。这就造成了所谓“共产党在装模作样的统治，老百姓在装模作样的服从”这一状态。于是有人提出，既然向专制当局正面抗争会招致镇压，为什幺非要正面抗争不可呢？表面上敷衍它，实际上不理它，难道不更明智吗？有人甚至宣称，今日中国，由于有越来越多的人采取了这种态度，共产党专制实际上已经徒留躯壳，逐渐消解于无形了。相比之下，自由派知识分子的不懈呼喊，民运人士的艰苦抗争，反而没起到多少实际的作用。<br />
<br />
　　这种观点带有明显的犬儒味。它试图让我们相信不争就是争，而且还胜过争。看来论者有所不知，在历史上，象这种“装模作样的统治-服从”的格局其实是中国专制统治的常态。正如白鲁洵（Lucian W．Pye）指出的那样：“中国统一性的秘密在于朝野双方共同装模作样，这就掩盖了双方的实力与局限。不论是在帝国时代还是在共产党时代，中国的政府一直做出享有全权的外观，但在实际上，政府贯彻落实政策的权力始终是相当有限的。与此同时，中国的社会则自行其是，几乎不向政府提什幺要求，从而维持政府享有全权的假象。这样，统治者和被统治者都倾向于保持一定的距离，装出和谐共处的模样。”简而言之，中国专制政治的特点本来就是“政府在装模作样的统治，老百姓在装模作样的服从”。<br />
<br />
　　中国的专制统治，除了在某些特殊阶段，例如毛泽东时代的某些阶段，“无产阶级专政”落实到每一个工厂、公社、街道、学校等最基层，再加上狂热的“群众专政”，整个社会确实会象奥威尔笔下的《一九八四年》那样，形同一个大监狱，“最高指示”能一竿子插到底，地方与个人毫无转圜变通的余地（其实严格说来也不尽如此）。在绝大部分情况下，中国式的专制都有其松动活泛的一面。最高当局的指示和政策固然不容批评，但它们通常都很含糊笼统，从而使得下面的人可以通融处理，便宜行事。即便有了明确的法令，但很少严格执行。专制制度本身是僵硬的，唯其如此，在实践中它不能不保留几分灵活，否则它就会因弦绷得太紧而易于断裂。这和传统的大家庭颇为类似。老话说：“不痴不聋，难为阿家翁。”统治者如同大家长，倘若事事较真，不给下面的人留余地，势必会和下面发生直接冲突，到头来不是鱼死就是网破。这是就统治者方面而言。至于在被统治者方面，他们当然要努力谋求自己的利益，而且一般也总是能够谋求到一定的利益，但通常不是采用公开地、理直气壮地坚持自己正当权益的办法，而是阳奉阴违，尽量利用钻空子、打马虎眼一类办法。和统治者有了矛盾，有了问题，靠下面说情，靠打通关节，靠贿赂拉拢，不是去谈判对话，不是去对簿公堂，不是去向政府施加压力。象“遇到红灯绕着走”这句话，固然是表明人们可以通过迂回的方式实现自己的某些目的，但它同时也意味着人们避免触犯专制者的权威，从而承认了专制者的权威。在这里，你可以说统治者的专制是很表面的，但它毕竟是在专制。你可以说老百姓的服从是装样子的，但它终究是服从。专制并不因此而被消解，相反，由于缺少正面的挑战，专制才得以苟延维系。<br />
<br />
　　且以言论自由为例。今日中国，如一般人所说，言论的自由度已经相当宽大。“自由度”一词容易使人以为自由主要是个量的问题，其实自由主要是个质的问题。风筝飞得再高也是不自由的，小鸟飞得再低也是自由的。现在，普通人在下边发发议论，甚至骂骂共产党，当局也不来管不来抓。可是，不来管不来抓是一回事，不能管不能抓是另一回事。就在最近，仍有一批异议人士遭到当局逮捕关押，社会上并未对此发出强大的抗议之声，更未能予以有效制止。可见中国仍未实现言论自由。一个国家有没有言论自由，不在于当权者愿意对不同政见容忍到什幺程度，而在于当权者是否有权力去压制不同政见。只有在人们学会了抵抗权力对言论的干涉企图时，真正的言论自由才得以实现。这就要求我们不能满足于怎样巧妙地从网眼里钻过去，而必须致力于粉碎那张专制之网。<br />
<br />
　　当然，软性的专制总比硬性的专制好。人们能够在假装服从之下自行其是，既是可以理解的，也不是没有积极意义的。问题是我们不能把这种积极意义估计过高。如果我们把这套假装服从的游戏玩得上了瘾，反把正面抗争视为迂腐、愚蠢或多余，那幺，专制的终结，权利的确立，法治的实行，自由民主制度的实现也许就遥遥无期了。<br />
<br />
15、犬儒式的自欺欺人<br />
<br />
　　和上节讲到的问题类似，犬儒病的又一表现是，在实现自由民主的过程中，夸大自利行为的意义，否定理念和理想主义的不可替代的作用。<br />
<br />
　　有人提出，既然自由民主这套制度是立足于个人利益之上，因此，只要大家追逐自利，无须多少理想主义精神，到头来也会自然而然地演化出一个自由民主的社会秩序，市场经济的秩序可以这样演化而成，政治民主的秩序也可这样演化而成。按照这种观点，自由民主制度无非是一套关于不同利益冲突碰撞的游戏规则而已，因此，只要现实中存在着不同利益的冲突碰撞，彼此冲来撞去，这套游戏规则就自然会慢慢建立起来了。譬如说，共产国家的权力结构本来就具有某种民主的外貌，全国人大就相当于民主制中的议会，有人便根据“屁股决定脑袋”的逻辑，期待人大因出于自利而致力于扩充自身权力，无意之中造成权力的分立与制衡局面。<br />
<br />
　　在民主运动正面攻坚屡攻不克的情况下，上述观点不免令人宽慰。考虑到在现实生活中常有“不虞效应”发生，“有心栽花花不活，无意插柳柳成荫”，正打歪着，歪打正着，上述观点甚至还显得有几分可信。不过，我对这种观点深表怀疑。道理很简单，打从远古以来，人们就一直是追逐自利的，为何不曾早早地就演化出自由的秩序、公正的秩序？何以在世界上竟还有专制的秩序、压迫的秩序存在？正因为人们一向是追逐自利的，所以才导致了彼此间的碰撞冲突。这种碰撞冲突的结果，并非总是导致自由而公正的秩序。它也很可能是导致一方对另一方的压迫，另一方对一方的臣服。除非人们在追求自己利益的同时，也承认别人的自利，或者反过来说，除非人们在自己的利益横遭侵犯时，敢于不惧风险起而抗争，否则就不可能形成公正的秩序而只能形成压迫的秩序。说到游戏规则，游戏规则也可以有两种。知青为上大学而相互竞争。建立严格的高考制度，分数面前人人平等，这是一种规则；凭特权，开后门，或者像张铁生那样交白卷，这何尝不是另一种。不同利益的冲突碰撞确实可能逐渐地形成一套游戏规则，但是那不一定自然而然地是一套好规则，它很可能是一套坏规则。除非我们坚持正确的理念并为之不懈的奋斗，否则我们不可能得到我们需要的好规则。<br />
<br />
　　以现今中国的情况为例。许多中共官员利用特权贪污腐败，化公为私。尽人皆知，唯有消除特权，实行法治，才能确保民众的利益。这就要求民众起来投入结束一党专政的斗争。可是投入反专制的斗争又是有极大风险的，它很可能招致残酷的镇压，到头来会蒙受更大的损害。因此，如果仅仅是出于自利的动机，你也许会认为还是放弃抗争，转而跟在别人后面也多少捞上一把才更保险，更实惠。如果大家都放弃公正的要求，只是去追逐被权势者榨取后剩下来的一点可怜的利益，我们又如何能得到一个自由而公正的秩序呢？由此可见，在促成制度转型的过程中，我们必须要有理想主义，要有英雄主义。<br />
<br />
　　从表面上看，理想主义、英雄主义意味着敢于为了赢得更大的利益而甘冒失去仅有利益的巨大风险，这好象是自相矛盾，好象和赌博差不多。问题在于，当我们反抗强权，我们不仅是为了得到更大的利益，我们首先是为了赢得自由，赢得公正，赢得自尊。这是比单纯的物质欲望更重要、也更富人性的东西。<br />
<br />
　　以上批评到的那些观点，它们都没有放弃对理想的向往，但却放弃了为理想的斗争，以为理想社会的实现可以不经过为理想的艰苦奋斗。这显然是犬儒式的自欺欺人。当人们出于挫折、沮丧，或出于恐惧、诱惑，脱离了为理想而奋斗的行列，又不愿意被别人视为背叛理想，他们就会说他们正在“曲线救国”，争辩说不抗争倒比抗争还有效果。吃不着葡萄就说葡萄会自己掉下来，用不着再去跳。<br />
<br />
　　在论及自由民主制度实现的各种条件时，学者们提到了许多因素，例如经济的发展，教育的普及，中产阶级的兴起，以及诸如此类。这些因素无疑都是有利的，但经验证明它们并不一定都是必要的。我更愿意强调理念，强调理想，强调抗争。没有自由理念，就没有自由社会；没有民主派，就没有民主制。<br />
<br />
16、克服犬儒，战胜专制<br />
<br />
　　如前所言，在共产专制统治的末期，协同这种统治的心态只剩下犬儒主义，因此，只有克服了犬儒主义，我们才能战胜专制。众所周知，顺民与暴民本是同一枚钱币的两面。因此，只有克服了犬儒主义，我们也才能保证平稳的制度转型。<br />
<br />
　　犬儒病是心理病。指出它的病症，分析它的成因，在很大程度上就是对它的治疗。犬儒常常不肯承认自己是犬儒。他们每每要编造出各种似是而非的理论或说词，把犬儒心态予以掩饰，予以合理化。这使得治疗犬儒病变得很复杂，但也因此而成为可能。正因为大多数犬儒都不能心安理得地当犬儒，所以，指出他们害了犬儒病就可能会激励起他们去克服它。<br />
<br />
　　此外，分析犬儒病的成因也很重要。一般人总是由于理想在现实中碰壁才对理想失去信心的，这就要求我们对理想碰壁的原因作出令人信服的解释。注意：在这里，我们一定要排除那些貌似深刻实则空洞的解释。例如把八九民运的失败归因于中国人的整体的劣根性，归因于共产党本质的邪恶性，或者是归因于我们的传统、我们的祖先的劣根性。既然这类劣根性按定义就是极难改变的，起码是在一个相当长的时期内极难改变的，那岂不意味着中国民运至少是在一个相当长的时期内几乎不可能取得成功吗？除非我们让人们相信，民运的失败不是注定的，相关因素的变化消长是可以较快就得到改进的，否则我们如何能让人们恢复信心？六四后不久便有人说，秀才造反，三年不成。说这话的人忘记了，中共政权自四九年来所遇到的最大的一次危机，恰恰就是让秀才们给促成的。问题不在于是否一定会由知识分子充当变革的主角，问题在于能否以理性、以理念作主导，问题在于是否还对理性有信心。<br />
<br />
　　犬儒由于自己无信仰，也常常怀疑别人有信仰。一个自愿为信仰献身的人，其本身的存在就证明了信仰的真实。这就是说，榜样具有治疗犬儒病的功效。<br />
<br />
　　不过，有些犬儒病入膏肓，一般的治法恐难奏效。他们不相信能治疗，所以拒绝接受治疗。他们不相信讲道理，所以你讲的道理他们听都不听。榜样对他们也无疗效。他们正是因为看到理想主义者没有好下场才变成犬儒的，所以，那些为了理想受苦受难的榜样很可能只会成为他们犬儒主义的证明：“你看，他倒霉了吧。”这就需要另一味药――成功。应该看到，现今犬儒病的流行，正是先前失败留下的苦果，是巨大创痛的滞后反应。理想主义者不能一味地愤世嫉俗，怨天尤人。我们必须力争在现实中赢得成功，从微小的成功开始，并不断的积累成功，然后以我们的成功去治愈他人的犬儒，然后再以更大的实力去赢得更大的成功。<br />
<br />
　　再有就是时间。不错，时间会导致遗忘，遗忘当年的痛苦，但同时也遗忘当年的恐惧。人心有强大的自我修复能力，它不会长久的沉沦。更不必说还有新陈代谢，专制对人心的扭曲是后天的，它并不能遗传；灵魂的自由却是先天的，每一次新生就是一个开端，就是一种希望。<br />
<br />
――《北京之春》1998年6月号</div>

							
							
							
							
