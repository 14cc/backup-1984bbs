无论是毛时代还是现今，由红色娘子军到黄色娘子军，在中共专制体制的框架下，无论是政治运动的强力扭曲，还是一切向钱看的紊乱失控，都不是正常社会下自然的女性形象。 <br />
<br />
由李纯纯编著、香港出版的《超级性大国》，直面现实，多方搜集资料实例，列举的数据，令人触目惊心、感叹深思。<br />
<br />
（一）2000万中国黄色娘子军 执世界牛耳 <br />
<br />
据学者估计，今日中国大陆的性工作者人数，保守估计在2000万至3000万之间，不单在绝对数量上稳居世界第一，就连在人口比例上，中国的黄业都“傲视全球”。 <br />
<br />
无论是大城市还是小县镇，都莫不存在着不计其数、各式各样的色情场所，K场、歌舞厅、桑拿、洗脚店、按摩、推拿场、发廊、酒店和街上的流莺暗娼，层出不穷，比比皆是。 <br />
<br />
卖淫业在中国遍地开花，更惊人的是中国同样是全球最大的妓女输出国，为数远在10万以上。妓女大军所向披靡。不单在东南亚与欧美，连阿富汗与阿联酋等保守的的伊斯兰国家，也有中国妓女“抢滩”掘金！ <br />
<br />
（二）一切向钱看 繁荣娼盛 <br />
<br />
八九“June Fourth”的坦克枪炮碾碎了自由民主的理想。邓小平要老百姓抛开道德是非，“白猫黑猫，抓到老鼠就是好猫”、“发展是硬道理”。人们一切向钱看，使中国“繁荣娼盛”。 <br />
<br />
温饱思淫欲，“先富起来”的人们，为性产业提供了市场。贫富差距越来越大，四川、湖南、贵州、江西、河南等贫困地区的工人、农民的女儿到东南沿海城市当起了小姐。回乡的卖淫女成为当地民众发财致富的象征，吸引了更多的怀有淘金梦的姐妹，壮大了黄色娘子军的队伍。 <br />
<br />
“性”旺发达，人肉市场由沿海地区向内地迅速扩大。中共腐败的滋养和权力的保护，在民间顺口溜中略见一斑。“下岗妹，挺胸坐进夜总会；陪大款，挣小费，开发身体养爹娘……谁敢讲：没地位，昨晚我陪书记睡！我的客，都要票，不是领导哪能报？……老封建，旧孔教，共产党人都不要。谁敢讲：道德坏，哼！我劝首长别休丑太太！选靠山，定要牢，搞定书记第一条。” <br />
<br />
性产业依附军警共营生存，公安在扫黄中，不但可以通过收容遣送制度进行罚款“创收发财”，甚至不少执法者可以得到免费的性服务。因此，不少性产业根本就是同军警合办的，提供了安全保证，军队或公安的招待所客似云来。 <br />
<br />
（三）上行下效 荒淫无耻 <br />
<br />
在Jiang Zemin的“三讲”和“三个代表”之后，中国淫业迈入黄金时代。在“开放大西北”的口号下，就连“人间净土”西藏都充斥娼妓而成为爱滋病的高风险区。 <br />
<br />
2000万性产业者创GDP 6%，性消费带动旅游、宾馆、赌博、服装、化妆品、医药、租房等消费，年社会总消费额就达10000亿以上。 <br />
<br />
当局睁一眼闭一眼，扫黄不过是象征性的应景而已。公安机关每年查获卖淫嫖娼案件14万多起，抓获25万卖淫嫖娼人员，查获率只有10%，茫茫“肉海”的小浪花，抓的都是没有后台的替罪羊。 <br />
<br />
Jiang Zemin与宋祖英的风流韵事，人人皆知；Jiang Zemin与陈至立的暧昧关系，似有耳闻……上行下效，官员包二奶、养情人、嫖妓、嫖幼，领风气之先，富豪、大款也乐此不疲。 <br />
