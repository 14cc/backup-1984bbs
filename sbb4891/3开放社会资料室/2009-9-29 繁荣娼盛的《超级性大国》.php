“没有情人是废物，找六七个才是人物，找百八十个才是大人物。” 江苏省建设厅厅长徐其耀，有情妇146位。海南省纺织局局长李庆普藏有236个女人的毛发。湖北原丹江口市委书记张二江情妇有107个。天津政协主席宋平顺的情妇有50多个。上海市委书记陈良宇的情妇有11个。 <br />
