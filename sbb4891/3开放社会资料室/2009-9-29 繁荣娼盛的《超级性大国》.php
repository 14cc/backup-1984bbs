2009年6月，中共4名省部级大贪翻船，水落情妇名人出。广东省政协主席陈绍基有广东卫视“美女主播”李泳；浙江省纪委书记王华元有商人陈某送上的“过水货”王美玲（私生一子）；深圳市市长许宗衡有赴港定居的明星；最高法院副院长黄松有专吃“嫩草”，嗜好处女，采阴补阳；前证监会副主席王益有某电视台女主持人…… <br />
