繁荣娼盛的超级性大国<br />
<br />
作者：沉静<br />
<br />
在妇联成立60周年座谈会上，中共人大常委会副委员长陈至立说，在党的领导下，广大妇女投身祖国建设，走过了60年光辉历程，成为推动社会发展进步的“半边天”。广大妇女自尊、自信、自立、自强的意识不断增强，综合素质不断提升，视野更加开阔……<br />
<br />
我脑海里浮现的是，跳忠字舞、喊口号的红卫兵，上山下乡和男人一样拼体力劳动的铁姑娘，打小报告的马列主义老太太，卖淫拉客的小姐，攀附权贵的情妇，权色交易、不择手段的女贪官…… <br />
