<br />
书摘<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;儒学在中国史上遭遇困境不自现代始。孔子之后有杨、墨，特别是墨家的挑战，这是第一次困境。汉晋之际有新道家反周孔名教的运动，这是第二次困境。这一次困境的时间特别长，因为继反名教之后便是佛教长期支配中国的思想界和民间信仰。第三次困境发生在晚明。由于泰州学派的风行一时，遂有“三教合一”运动的兴起，李卓吾承王阳明之说而更进一步，公开宣称“不以孔子之是非为是非”。这次运动虽未如魏晋时代明标“反名教”之名，但实际也相去不远，所以才引起顾炎武、黄宗羲等人的大声疾呼。黄宗羲在《明儒学案・泰州学案序》中更明白地指出：泰州学派到了颜山农、何心隐诸人手上，“已非复名教所能羁绊”。<br />
如果从历史背景着眼，我们不难看出，这三次反儒学的思想运动都爆发在中国社会解体的时代。解体的幅度有大有小，深度也颇不相同，因此对儒学的冲击也有或强或弱之异。但以现代的眼光看，上述三次社会解体都没有突破中国文化传统的大格局。儒学在经过一番自我调整之后，仍能脱出困境，恢复活力。<br />
现代儒学的困境则远非以往的情况可比。自19世纪中叶以来，中国社会在西方势力冲击之下开始了一个长期而全面的解体过程；这个过程事实上今天还没有走到终点。由于社会解体的长期性和全面性，儒学所面临的困境也是空前的。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;为什么儒学的困境和社会解体的程度有这样密切的关联呢？这首先要从儒学的性质说起。儒学不只是一种单纯的哲学或宗教，而是一套全面安排人间秩序的思想系统，从一个人自生至死的整个历程，到家、国、天下的构成，都在儒学的范围之内。在两千多年中，通过政治、社会、经济、教育种种制度的建立，儒学已一步步进入国人的日常生活的每一角落。我们常常听人说儒学是中国文化的主流。这句话如果确有所指，则儒学决不能限于历代儒家经典中的教义，而必须包括受儒家教义影响而形成的生活方式，特别是制度化的生活方式。但是我们必须紧接着澄清两点必有的误会：第一，中国文化包罗甚广，自然不限于儒学。我们说儒学弥漫在中国人日常生活中的每一角落，并不意味中国文化整个是儒学的实现。事实上，在每一角落中我们也都可以找到非儒学的成分。不过整体地看，确没有任何其他一家的教义散布得像儒学那样全面。第二，我们并不天真地认为儒学在日常生活中的制度化即是儒学的充分实现，缘饰和歪曲都是制度过程中所确实发生过的现象。儒学基本上是要求实践的，无法长期停留在思辨的层次，从个人的心性修养到制度化显然都是归宿到实践。观念一落实到生活中必然会因种种主观和客观条件的限制而发生变化，因此歪曲更是不可避免的。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;我们在此只是指出儒学通过制度化而在很大的程度上支配着传统文化的这个事实。我们并没有下道德判断。指出这个事实正是为了说明儒学的现代困境的根源所在。传统的儒学诚然不能和传统的制度划等号，但前者确托身于后者。19世纪中叶以来，传统的制度开始崩溃。比较敏感的人便不免把制度运作的失灵归咎于儒学。例如太平天国时代的汪士铎，其为为曾国藩、胡林翼所器重的谋士，而曾、胡都是所谓“理学名臣”。但他在《悔翁日记》中便屡次痛斥儒学，不但骂程、朱理学，而且也鄙薄孔、孟。他是一个讲功利的人，思想上倾向法家，在学术思想史上，汪士铎自然占不到任何地位，然而他反儒学的激烈态度却是一个信号。更可注意的是他并没有接触到“西学”；他是从传统内部出来反儒学的人。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;中国近 代反儒学的运动要到“五四”时期才发展到全面的阶段。但“五四”以前已有一个很长的酝酿阶段。鸦片战争以后，中国人最初只承认在“船坚炮利”方面不如西方，所以冯桂芬的《采西学议》仅限于西方的科技。到了清末变法时期，中国人才正式承认政治制度落伍了。康有为、梁启超、谭嗣同的鲜明态度不必说了，即使以提倡“旧学为体，新学为用”著名的张之洞也已对传统的政治制度失去了信心，因为他所谓的“新学”包括了“西政”在内。<br />
在全面社会解体的过程中，政治制度是最早崩坏的一角，紧接着便是一切社会制度的全面动摇。谭嗣同的“冲决罗网”已开“五四”的先河。不过他毕竟还没有全面地反儒学，只找出荀子做替死鬼；他的最大著作也标“仁学”之名。这也许是因为康有为依然要假借孔子的旗号。从戊戌政变到“五四”不过二十年，但这二十年间中国传统制度的全面瓦解已表面化。从家族婚姻、乡里、学校各种制度到风俗习惯，其中已没有任何一部分是可以站得住的了。“五四”全面反传统的运动便是在这种形势下逼出来的。“五四”又号称“新文化运动”，其实所谓“新文化”即是西方文化，而以“民主”与“科学”为其主要内容。儒学则在这一运动中首当其冲，成为反传统的最主要的对象。<br />
