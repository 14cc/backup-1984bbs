																											<div id="postmessage_845702" class="t_msgfont">1938年，在日军重兵围剿下，抗联八军的大部分领导牺牲，也有的投降了敌人。部队伤亡很大。在不到一个月的时间里，谢文东本人先后有八位亲人被日本兵杀害，他的队伍也只剩下十几个人了。<br />
投降<br />
　　1939年春谢文东在抗日战争处于艰苦逆境的时候，投降了日本侵略军，背叛了祖国和人民。 　　谢文东投降后，曾赴日本东京受到天皇裕仁的召见，无耻地向日本政府表示反省，跪在所谓“忠魂碑”前忏悔谢罪。从此，谢文东充当了日本帝国主义忠实奴才。回到勃利后，日本给谢文东盖了5间砖瓦房，从小五站大义屯划给熟地40垧和牧场，并任其为勃利县日满协和会名誉会长，鸡西城子河煤矿把头和劳工大队长。从此，谢文东便死心塌地靠紧了侵略者，开始欺诈人民群众。1939年在城子河煤矿当把头时，每天只发给工人几元钱，还要扣除各种费用和他的工资。发给部分吸大烟的人的烟份，他也要扣一些，转手高价发横财。1942年实行劳工法，重要产业与军事工程进行强制性摊派劳工。勃利每年出六七百人到煤矿当劳工，劳力不足时还要“抓劳工”下井背煤，冻死、饿死、病死的矿工白骨成堆。<br />
清剿<br />
　　1945年10月谢文东在小五站大义屯，以养病为名打出了国民党的旗帜，组建第五战区中央先遣军第三军，被国民党委任军长。在勃利、桦南等地组建十几个团1500余人。同年11月合江省主席李延禄，以抗日联军的老关系，给谢多次写信，让他认清形势，相信共产党的政策，不要上国民党的当。经过谈判，他阳奉阴违同意将其部收编为三江人民自治军。但他暗地却把队伍调往他地待机叛变。11月下旬国民党特派员武康赶到勃利，策反谢文东，谢遂将队伍改编为国民党“东北第十五集团军第一先遣军”，谢文东被委任上将总司令经常流窜于勃利、依兰、林口、鸡宁(西)等县与共产党和人民为敌。烧杀抢掠，破坏解放区后防根据地。 　　1946年3月由方强领导的三五九旅部队进驻勃利、桦南后，几次和谢文东交战，谢被打得四处逃窜。但谢文东仍然野心不死。国民党在西三道河子召开的整编会议上，特派员杨义儒以代表国民党东北剿共总司令的名义委任谢文东为合江省保安军第二集团军中将司令官。当谢文东走向“自取灭亡”的时候，共产党为给他一条生路，又派员劝其改邪归正。而谢文东执迷不悟继续与人民为敌到底。 　　1946年秋，三五九旅和合江军区对谢文东进行围剿，谢无站脚之处，企图窜到牡丹江逃往长春。路经连珠河发现八路军大部队，又返回八道沟，10月流窜到夹皮沟。 　　1946年11月20日谢文东带领几个人，逃到四道河子至五虎咀子之间的一个小山神庙时，被三五九旅八团五连副连长李玉清和二排长刘书颜所率队伍活捉。勃利县政府于12月3日在勃利县政府门前，召开公审大会，会后在碾子河西岸谢被处决，死年60岁。</div>

							
							
							
							
