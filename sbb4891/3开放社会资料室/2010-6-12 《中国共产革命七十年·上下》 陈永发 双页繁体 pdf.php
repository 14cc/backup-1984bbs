书籍简介：<br />
中共建党七十年来，对中国，对全球都有举足轻重的影响。尤其近年来海峡两岸互动频仍，风云诡谲，我们更须了解其历史背景、意识形态、政治现况与困境、才能对中共党史有全盘的理解。<br />
<br />
本书从中国近现代史的角度来研究中国共产党的历史，将其分为三个阶段：革命夺权、不断革命、告别革命；并将中国共产党革命放在中国追求现代化的长期脉络中来观察，深入探讨中共历史上的三大问题――民族主义、基层权力结构、思想改造和控制。为读者提供一个广阔的视野，从而对中共的历史、现状，甚至未来，有更精到、更切实的认识。<br />
