呃，然后今天我准备了一些说的内容带了一个稿纸，这是为了约束我自己。主要让大家不要受到什么迫害,怕我满嘴跑火车,开始了啊：<br />
<br />
各位领导,各位老师,各位同学大家好,大家知道中国为什么成为不了文化大国吗？因为在我们大部分讲话的时候,各位领导永远都是放在第一位的,而各位领导都是没有文化的。不光是这样，他们还是惧怕文化的，是审查文化的，但是呢,他们又能够控制文化,所以说这个国家怎么够成为文化大国呢?各位领导你们说呢？<br />
<br />
其实中国是有成为一个文化大国的潜力的。我给大家讲个故事。我要主编一本杂志,到现在都没有出版。然后呢，咱们宪法上有规定啊，每个公民都拥有出版的自由，但是呢我们的王法又有规定，就是领导有不让你出版的自由。<br />
<br />
这个杂志呢很多地方在审查上遇到了一些问题，里面有一副漫画。漫画是一张图，主人公是个男的，他没有穿衣服。当然这是不可以了。因为相关的法律法规规定不能露出那个阴部在公开的出版物上。但这是我认可了，我觉得没有问题。所以把那个杂志特别大的一个LOGO就挡在它那个不合法的部位，然后后来这些出版社的审查人员就告诉我说 这个不可以，你把这个人的中间这个地方啊挡住了，你这是在暗喻“党中央”。<br />
<br />
我的反应和大家一样啊，我被雷到了。我当时脑子里就在想,有时候把你这个惊天地泣鬼神的想象力，用在文艺创作上，而不是用在文艺审查上那该有多好。通过这个故事我想告诉大家，其实大家都是很有想象力的啊，但是很多事情我们只能想，我们不能去做,不能写,甚至很多场合不能说啊。呃...我们的限制太多了，这是一个限制级的国家。在限制级的国家里怎么可能产生非常丰富的文化呢？我已经算是一个自我限制很少的一个同志了，但是在我落笔的时候呢，我就会情不自禁的想，警察不能写，领导不能写，政策不能写，制度不能写,司法不能写，很多历史不能写，西藏不能写，新疆不能写，集会不能写，游行不能写，黄色不能写，低俗不能写，艺术不能写，高雅我又写不出。抱歉啊，我真的写不出甚么高雅的东西，我又不是余秋雨。<br />
<br />
在网路上发表的一些文章啊,尺度已经算是比较大的。有很多写剧本的朋友,我一些写剧本的朋友,包括宁财神啊写一些话剧,还有电影剧本编剧,他们非常的痛苦。在这样的一个文化环境下,我就开始想，如何成为一个文化大国？除非全世界就剩下中国,朝鲜还有阿富汗。朝鲜是文化禁地，大家都知道。然后阿富汗是因为国内的局势正在搞不清楚，他们还顾不上文化。但纵然这样,他们都有作家写出了《追风筝的人》。当然比较遗憾的是这不是阿富汗出版的。我想,一旦阿富汗搞清楚了，也不是没有可能去超过中国。<br />
<br />
呃...我们所谓的在国际交流上不能再拿那些四大名著和孔孟之道来说事，这样就像相亲的时候女方问你有没有钱，你说你祖宗十八辈上有钱，这是没用的。这个就悲剧的造成，这个和大家没有任何的关系,虽然说,通往朝鲜的道路，是由每一个沉默的人铺就的。但是,某一方面我们当然要比朝鲜强很多啊，因为大家也都知道朝鲜是什么样子的。另外一方面呢,我相信在座的大家,其实很多人,大家并不沉默啊,大家只是被和谐了。<br />
<br />
呃... 在中国的这个扫黄史上,可能很多同学，我想大家应该都知道，毕竟是大学生，大家都知道虽然现在的一些教材上并没有具体内容，就是邓丽君和刘文正都是黄色的，下流淫秽，但是呢因为听的人多了他们就变成了黄色和下流，他们就不淫秽了，但是呢到最后全国人民都在听，所以他们既不黄色也不下流了。<br />
<br />
如果我们都能够来反对文化的审查，让我们的屏蔽词里除了那些反人类的词汇不再有其他词汇，那我们才有可能去创造出一个文化大国，哪怕是在这个过程当中你我的名字都会进入这个屏蔽词库，但我相信一个屏蔽词库是有它的最大载重量的，每一个新增加的词汇都是在加速它的灭亡。<br />
<br />
我希望我们的新闻媒体的从业者，我们的学生老师，每一个文化的从业者爱好者，包括我们的管理者，可以努力让我们的屏蔽和审查越来越少。我们的领导们，因为这个领导是和大家分开的啊，所以我们的领导我们的政府可以有足够的自信让文化更加的开放。我知道我们的领导很喜欢向国外输出我们的文化，觉得这是一个强国的象征，但是以现有的文化我觉得实在是输不出去的。我们在创造文化作品的时候，每一个作者每一个从业人员，他们在时刻进行着自我的审查，在这样一个创作环境下，我觉得怎么能产生像样的文化作品呢？在全世界范围内，你把文艺作品阉割得像新闻联播一样给外国人看，然后企图输出中国的文化，你当外国人是外星人啊。<br />
<br />
中国是不是在经济上真正的崛起了，这个呢要等我们的房地产业崩盘了以后再看，现在一切都不好说，但是如果一个国家在文化上真正的崛起了，那它真的是一个强国，而且我想应该永远不会有崩盘的危险。<br />
<br />
最后说回到我们的屏蔽词库，一个屏蔽词库里的词越势多，我们这个国家的文化就会越势弱。当然我们的政府会给大家很多的解释，他们会告诉你，我们这么做是为了保护青少年，是为了社会的稳定，文化是自由的。所以他们有权屏蔽任何危害青少年，破坏社会稳定的资讯和文化。但是如果你认同了,迟早有一天你会发现，你在控诉你的遭遇的时候，他们会将你屏蔽，罪名是破坏社会稳定。到了最后，凡是不利于统治阶层的，不利于他们获得利益的言论，都是破坏社会的稳定，都是危害青少年。如果我们当时容忍了绿霸花季护航的话，那么我们就会看到绿霸花甲护航，到那个时候就不光光是文化的东西了,所以同学们,我们不能让这一天的到来。否则，在以后，在若干年以后，在你的孙子们，通过卫星接收到的电子课本的历史书上,我们都会是笑料。<br />
<br />
所以…谢谢大家。</div>

							
							
							
							
