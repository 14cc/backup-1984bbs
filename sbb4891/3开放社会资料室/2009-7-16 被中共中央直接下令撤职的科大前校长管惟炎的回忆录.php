我们讲话比较随便。我们几个校长讲完后，在座的同学、学员愿意上来讲也欢迎，还可以讨论。<br />
<br />
刚才有一个同学跟我说，你们学校领导现在是不是受到很大的压力。我在食堂吃饭时，经常也有同学围着问，学校是不是受到上面的压力。我可以回答大家，直到现在为止，我们没有受到任何压力。当然，科大办得究竟怎样评论不一，严老讲科大办成了招风的大树，可能会有很多人谈论科大。招风嘛就是受议论了。议论可能是有褒有贬，有说你们这样搞很好，也有说你们搞糟了的。这些议论是有的，但这不是上面的压力，这是社会舆论。你要到北京、上海或其他地方，可能有人说你们科大好得很，也有人说你们科大思想政治工作薄弱，所以现在学生闹事，而且在全国带头，影响了我们。《人民日报》前几天连续发表了五篇文章，说我们很好，那可能有点夸张，实际上我们并没有做得那么好。不久前确是有一位同志――当然是一个很好的同志，是很关心我们学校的同志――提醒我们说，你们的思想政治工作可能是抓得不够的，光是讲民主，光讲自由，到时候会出问题的。我想这是好意。我自己作为学校的一个领导，可以很坦率地承认，他批评得很有道理。总结过去三十多年来所谓的思想政治工作的经验，特别是其中的两个错误的东西，一个就是整人。过去所谓的思想政治工作很大一部分内容就是整人，例如反右、插白旗、文化大革命，都是以阶级斗争为纲，就是整人的，这个我们坚决不干。不干可能并不太难，坚决不干，有时很难。就是说人家都说这人是有问题的，那么你是不是也跟着说他是有问题的，不能这样吧。就是说，我们总结了三十多年的经验，学会了实事求是。比如说反右派，开始大家都大鸣大放，有的人在那讲话，起初大家可能也很赞同，后来突然说工人阶级说话了，说这个人变成右派了，结果很多人就转向批判他了。批判他的人不能说都是坏人吧，但那是没有坚持实事求是的态度，我们坚决不这样干。因为这样不仅对被整的人很不好，而且对我们党来说也非常不好。这样最后会鸦雀无声，我们党将自食其果。这个教训是很深刻的。另外还有条经验，就是我们不要搞形式主义的东西。人家不听你偏讲，是没有什么效果的，早请示，晚汇报，天天读，未必真有效果。我们不要人人过关，逼人家说谎话。我们厉行两个坚决不干。那究竟应该怎么干呢？上次我在给大家上党课时出了这个考题，叫大家想，现在的情况下，怎样倡导精神文明？那时同学还没有怎么活动。那次党课上我讲了，北大的学生提出要振兴中华，怎么振兴啊？这个口号不够完备。清华的同学提出从现在做起，从我做起，做什么呀？这口号也不够清楚嘛。科大嘛，你是一棵招风的大树，你也应该能拟出一个口号来，看来这几天我们的同学们是不是已经回答了这个问题了，就是要求民主，要求自由。<br />
<br />
Page 460<br />
<br />
今天参加这个会我想起来了，我们也不能妄自菲薄。说我们完全没有做思想政治工作，我们不是还办了个业余党校吗？人家批评我们，你们科大是不是只讲民主不要党的领导。怎么不要党的领导？我们还办党校嘛！党校还学习党的基本知识嘛！我们还有这么多同学很热心地完全自觉自愿地来参加党校的学习，这确实是不容易的。胡耀邦同志分析说，马列主义现在又处于低潮，全世界都处于低潮，这是一个大胆的论断。总之现在讲马列主义并不吃香。你讲马列主义可能有人不爱听，你讲党的领导有的人也不爱听。前些时候在北大有一个学生在台上讲，我不是共产党员，下面鼓掌；说我永远不会是共产党员，大家鼓得更厉害。就是说现在是马列主义低潮。现在我们这些同学在所谓低潮的时候还愿意来上党课，还愿意来学习党的基本知识，这是不容易的，起码不是投机的吧！我觉得并不是我们科技大学办得就是一团糟，就没有党的作用，也没有党的影响。<br />
<br />
Page 461<br />
<br />
邓小平同志在回答中曾根时讲了几句话――那时他提出政治制度改革的问题。他讲了政治体制改革的三个目标：反对官僚主义。我们同学也提出了打倒官僚主义，这是一致的嘛；调动人民群众的积极性，使党和国家的机关有活力。接着讲政治体制改革还是要在党的领导下进行，不要忘记四个坚持。但是紧接着邓小平同志又讲了一句，党的领导要改善。他也讲我们还是要坚持马列主义，紧接着又讲了一句，马列主义要发展。这就对了。坚持并不是僵化，把过去的东西百分之百地不能动。现在我们科大和全国学生中发生的问题，是不是去搬马列主义经典著作就能回答清楚，我想不一定，我们要自己来分析。如果有人把现在全国学生中出现的现象说清楚了，那就是发展了马克思主义，就是高水平。如果死背马列主义，谁都会背，背是不管用的。现在要开始解决中国如何搞好政治体制改革的问题，这个禁区现在打破了。以前政治不能讨论，一讨论就有问题，是不是你要反党，看来现在这个禁区打破了。但是究竟怎么搞好中国的政治，马列主义不能照搬，还要结合中国的实际。西方的东西也不能照搬。我们中国是社会主义，西方――虽然欧洲也标榜社会主义――是资本主义。而且西方的资本主义各个国家也都不一样，各有千秋。所以照抄照搬别国家的经验、口号，恐怕不能解决中国的问题，包括马列主义。现在大家都承认马列主义应该多样化，所以我们要了解我们中国的国情，发展马列主义，改善党的领导。我个人理解小平同志提出的改善党的领导含有这样的含义：我们共产党是执政党，很长时间以来民主的精神可能不够，民主的意识可能不够。党的领导可能过分鲜明，所以慢慢地就忘了一点，就是说我们党所以能领导群众，是因为你能代表群众，代表群众的利益、代表群众的心声。而我们全体党员应该真正是群众的公仆，真正是为了群众的利益而牺牲自己的人，而不是到共产党内来谋取官职，谋取私利，不应当是这样的人。党的领导可以有两种理解（这是我个人的理解，错误的大家可以讨论），一种理解是权力，我有领导权，另一种理解就是思想上的领导，就是我们有威信。权力和威信是区别的。我们共产党人领导中国革命，当初我们处于无权的地位，经过艰苦的斗争，取得领导权，这是无可非议的。资本主义国家的政党也是要夺取领导权的。我们党就是要代表人民的利益，夺取政权并掌握政权。一个政党是否拥有权力，是一回事，但你在人民群众中是不是有享有很高的威信，那是另一回事，这就看你这个党的作为了。你权力可能很大，什么事情都要通过你，你说了算，但是老百姓脑子里并不佩服你，光靠权力是不灵的，权力和威信完全是两回事。你的权力非常之大也没用，你的权力大到可以砍下一个人的脑袋，但你要这个脑袋佩服你可不容易，要人家说你了不起，有学问或者说你这个人是正派的，是敢讲真话的，是代表我们利益的，这可不容易。这不是说有权力就可以做到的，有权力可以把人头砍下来，但不能改变人头脑内部的那个信仰。我们共产党人现在要改善党的领导，就不能只靠我们有权，就应当更强调我们在人民群众中要有威信，是群众信服我们，确认我们讲的是真理，有道理。我们的一举一动都是代表人民的利益。我们究竟在为谁服务？我们要反思，为什么一个人讲我不是共产党员，大家会鼓掌，讲将来也不是共产党员，大家鼓掌得更厉害。这些事不得不让我们共产党员要反思，不能简单地责备这个同学，也不能责备下面鼓掌的同学。这不是哪个人个人的行为，这是因为我们党三十年来在领导中国的建设事业上犯了很多很多错误。恩格斯曾说过，原始社会里的酋长的权力比资本主义社会里的一个警察的权力还要小，但他的威信却比资本主义国家的总统还要高。这里的意思仍然是权力与威信是两回事。我们讲共产党要领导中国，邓小平同志讲要坚持党的领导，但是他立刻又讲要改善党的领导。改善党的领导，我想就是说不应该只强调说我们不能放弃政权。当然，我们经过艰苦斗争、流血牺牲得来的政权，我们不能轻易拱手让出。任何一个政党都是如此。我们共产党相信我们自己掌握着真理，当然不会轻易放弃领导权，我的意思不是说我们不要领导权了，拱手让出权来。但如果我们只强调我有领导权，你必须听我的。那么你用压制的办法是可以的，你甚至可以把人抓起来，把脑袋砍下来，可是人家并不服你，这样能压制多久就很难说了。所谓改善党的领导，就是要使我们党真正在群众中享有威信，真正代表人民的呼声，为人民办事，这是最重要的。坚持马列主义是没有问题的，同时发展马列主义也是必须强调的，不能死扣教条，要结合中国的实际，结合实际中发生的问题来解决问题。当前我们学校里的学生和全国的一些学生，走出了校园，上街游行，要求民主，这个行动不能说是少数人别有用心蛊惑起来的，（热烈鼓掌），也不能说我们的学生是在破坏我们国家的安定团结，（热烈鼓掌）。是因为学生要促进中国的民主进程，他们的愿望和中央提出的政治体制改革是同一个愿望。至于采取的方式是不是合适，那么在这个问题上首先有一条，别的地方我不敢说，直到现在为止，至少我们科大的学生所采取的行动是完全合乎宪法和法律的，宪法和法律应该保障人民应该有的权利。但是，是不是也有的人不赞成，包括我们一部分同学和校外的同志，他们不完全赞成上街游行这种做法，那应该承认确有这样的人存在。我们希望大家要有追求民主的精神，每个人可以坚持自己的观点，自己的信念。但是也要尊重别人的信念，别人的观点，不要强加于人，这就是民主精神。不民主就是凡事你非得按我的意见不可。有人说我们的游行不好就让他说去，不主张游行的人不要说去游行的人是别有用心，破坏安定团结。但有的人要念书，不愿上街游行，其他同学也不要说他是学贼、狗腿子，这样不好。这样就把矛盾指向同学自己了，同学是无罪的。如果说我们的学生会在建立过程中不够慎重，不够充分发扬民主，这是学校的责任，千万不要使同学之间感觉到有压力。你不赞成上街游行，劝说可以，但不要强制性地把人抓起来。但他不去游行，你也不要骂他，你可以说服他，讲道理，可以宣传，我们不主张谩骂和恐吓。我今天听到消息说上海的学生最近比较平静，在考虑究竟下一步该怎么办。同济大学的同学要诉诸法律，我想这是对的，是一个好的办法。上街游行不一定能解决问题，我告你去，可能这是一个比较有效的办法。民主还要讲法治，自由也得有纪律，如果自由是谁愿意干什么就干什么，美国不可能建成现在这样一个富强的国家。我们应尽量避免变成自由就是放任。<br />
<br />
Page 465<br />
<br />
刚才有同学又问有没有压力。我想我们只要按照宪法和法律的规范，坚持四项基本原则，不违反学校的规定就不会有压力。从学校新领导班子上台的时候起，我们就主张“放”的方针。我们现在没有压力，如果有压力，我们也不怕。所谓“放”的方针，就是我们相信群众，我们相信群众不是故意要闹事的，而是要中国更好，更加民主，更加富强。我们相信群众，相信绝大多数群众都是通情达理的，决不会故意胡闹。前几天我们学校出现了很多小报，有人很担心，说学校对我们的小报怎么看。我当时就在那种场合讲，你们放心，你们写好了。也没有出什么事嘛！但后来确实出现了一些小报，同学之间互相攻击起来了，那我们要劝阻，变成人身攻击，那就没有什么意思了。我们为什么敢说“放”呢？（我们在座的都是愿意接受党的教育的，都是对马列主义有兴趣或有信仰的。）还有一个原因就是相信我们自己把握真理。有这两个相信――相信群众和相信我们拥有真理，我们就敢“放”。如果一个统治阶级觉得自己没有真理，他怎么敢放呢？放了以后，自己不是就要被推翻掉了吗？自己没有真理，想压迫人，剥削人，他享有特权，他一放的话，特权不就没有了吗？我们不是想要统治人民，我们想代表人民的利益。我们一是相信群众，一是相信我们手上有真理，群众也是相信真理的。我们学校的某一位领导曾经说过我们学校要追求真理的，这居然也受到有些同志的批评，说你追求真理是不是还要党的领导啊？追求真理是不是不要社会主义啦？我想追求真理和社会主义，和党的领导并不是矛盾的，因为我们相信我们党是掌握着真理的，相信只有社会主义才能代表中国人民的利益。那么你追求真理不正好吗？你以追求不就追求到我这里来了吗？所以你没有必要害怕人家追求真理嘛！怎么能人天生下来就相信你共产党呢？只有追求真理的人才能追求到我们这里来。好，我讲得太多了，下面还有几位校长要讲。<br />
