管惟炎（1928年8月18日－2003年3月20日），江苏如东人，物理学家。1957年毕业于苏联莫斯科大学物理系。1960年回国。历任中国科学院物理研究所所长，中国科学技术大学校长、研究生院院长，中国物理学会常务理事、秘书长。在执掌中科院物理研究所和中国科学技术大学期间推动了一部分制度改革。在学术上以超导研究知名。<br />
