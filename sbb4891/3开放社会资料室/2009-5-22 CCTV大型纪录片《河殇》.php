《河殇》<br />
《河殇》是中国中央电视台制作的六集电视纪录片（时称六集电视连续节目），在1988年6月16日首播；总撰稿人为苏晓康和王鲁湘，导演夏骏，但全片的主要内容最初策划者为学者谢选骏。该片播出后在中国社会引起了很大轰动，后被认为是陆肆事件的思想前导。<br />
<br />
《河殇》分为六集，分别为：《寻梦》、《命运》、《灵光》、《新纪元》、《忧患》和《蔚蓝色》。该片由对中华传统的黄土文明进行反思和批判入手，逐步引入对西方蔚蓝色文明的介绍，对包括“长城”和“龙”在内的许多长期被中国人引以为荣的事物进行了无情剖析和嘲讽，同时表达了对西方民主文明的向往。<br />
<br />
社会影响<br />
该片播出之后，除了在思想较为激进的青年学生中受到广泛欢迎之外，更一度得到以赵紫阳为代表的中共领导层的赏识，在中央电视台曾两度重播，片集在校园引起“河殇热”，学生热情讨论中国未来的出路与发展。<br />
<br />
1989年陆肆事件之后，中国大陆主流媒体在批判赵紫阳的同时，对《河殇》也进行了抨击，该片被认为是宣传“资产阶级自由化”、“虚无主义”思想的典型，自此陷入禁播状态，但现在民间仍然通过网络等媒体在小范围内流传。<br />
<br />
亦有声音表示：河殇所进行的中国传统文化批判和对西方文明的推崇有一定盲目性，未能较好的权衡中西文化实质差异和现在民主的实质而导致批判过激。<br />
<br />
和河殇一样，《狼图腾》一书也对汉文化进行批判，但并不涉及政治因素。而纪录片《大国崛起》也被认为是受了河殇影响。</div>

							
							
							
							
