													<h2>黄西</h2>
						
						
																											<div id="postmessage_852146" class="t_msgfont">　　黄西<br />
<br />
　　黄西在纽约参加“Comedy Central”电视频道试镜。在美国土生土长的第二代或第三代华裔喜剧演员上这个节目不算是新闻，但这将是第一位英语非母语的第一代华人，应邀在这个全美最热门的喜剧频道上演出，让最近演出邀约已应接不暇的黄西非常兴奋；但他在电话中的口气还是非常冷静，尽管这是他的最爱，“但这还是副业，一直到我能确定走这条路的收入充足稳定之前，还是要努力上班”。<br />
<br />
　　如果能支持生活的话，黄西承认他会考虑改行，因为“学术界不缺我这个中国人”。更重要的是，黄西已对他个人讲美式笑话，产生了一份使命感。<br />
<br />
　　2008年曾回北京在海淀表演的黄西意有所指的说，直到一个民族能开自己的玩笑，嘲讽自己的领袖与政治社会，这个民族才算成熟。除此之外，他希望自己以第一代移民的身分，能够透过自己的经验观察，说明移民的故事，“移民在社会中是无声的一群，可是移民社会中有许多有意思的事，也有许多被歧视与挣扎的事，就是因为语言与文化的障碍，自己表达不出来，又没有人帮忙表达”，黄西说：“这是一个机会，我愿意做个移民的代言人，说出移民乐观、奋斗、坚强的一面。”<br />
<br />
黄西-人物评价<br />
<br />
　　黄西<br />
<br />
　　出现在喜剧中心频道与众多电视广告的韩裔搞笑艺人艾咪安德森(Amy Anderson)也表示，当前在全国性搞笑圈活跃的亚裔搞笑艺人多是韩裔，华裔屈指可数，“也许华人个性较韩国人来说，还是比较严肃”。也认识黄西的她表示，以黄西的新人之姿表现确实有趣。<br />
<br />
　　在搞笑圈有12工龄历的她说，亚裔搞笑艺人须努力跳脱族裔给人的刻板印象，走出自己风格，否则久而久之，就会被视为没有原创性而被淘汰。“专业的亚裔演员，常会被放在显微镜下视图”，她说：“在美国搞笑圈里就是有这样不公平的双重标准。”<br />
<br />
　　对此，余智敏深有同感，身为华裔搞笑艺人，过去也常拿自己华人背景说笑：“我在俄勒冈州小镇长大，那里只有三个少数族裔：我一个华人、一个黑人，还有一个聪明人。当我去白人女友家按门铃时，她老爸开门看了我一下说：抱歉，我们没有订中餐馆外卖。”<br />
<br />
　　后来，好莱坞开端要求他用滑稽的中国口音表演，坚持不丑化中国人的他，决定改走自己的路，用自己父亲当年遭“排华法案”而买出生证的辛酸过去，改编出一剧“纸儿子”(Paper Son)，让人观后笑中带泪的演出，为他博得才子好评，而不再只是个“亚裔搞笑艺人”而已。<br />
<br />
　　他苦笑：“美国搞笑圈是很严酷的，好笑就是好笑，不好笑就是不好笑，不分族裔，创新才是搞笑艺人的唯一生存之道。”<br />
黄西生平简介 黄西（Joe Wong），曾在中国科学院攻读硕士，后获得德克萨斯州莱斯大学博士学位，全职工作从事科学研究。美国深夜节目收视率冠军的“大卫赖特曼秀”，2009年4月17日晚上破天荒邀请中国口音极重的黄西(Joe Wong)亮相，以英语讲美式笑话，近六分钟的演出，观众反应热烈。黄西一炮而红。<br />
<br />
黄西<br />
<br />
　　黄西生于1970年，是一位典型的留学生，念书取得学位，就业结婚生子留在美国。<br />
<br />
　　他毕业于中国吉林大学，主修化学。<br />
<br />
　　1994年到美国前在中科院研读。<br />
<br />
　　1999年取得德克萨斯州莱斯大学(Rice)生化博士学位。<br />
<br />
　　2000年到剑桥 (马萨诸塞州)一家跨国基因制药公司Sanofi-Aventis Genomics Center工作。<br />
<br />
　　白天，他在实验室正经做研究的科学家，到晚上就摇身一变成演员，“下了班，弄好孩子，准备好晚饭，等太太下班回来”，他就穿梭在新英格兰地区的酒吧、夜总会、俱乐部和大学礼堂，表演他的单口笑话，波士顿是他起家的地方。<br />
<br />
　　黄西在吉林大学时就喜欢写笑话和讲笑话，英文课念到“读者文摘”时，对其中的笑话集锦特别有兴趣。在德州念书时，为了排遣学生生活的艰苦无聊，除了“读者文摘”，他开始读其他的幽默或笑话的书，马克吐温与导演伍迪艾伦的作品对他的启发很大，让他了解美式幽默与美式笑话。但他在莱斯大学想参加写作创作班却被拒绝，原因是文笔不够好。<br />
<br />
黄西-成长经历<br />
<br />
　　黄西<br />
<br />
　　到麻州工作后，他利用时间报名就读“笑话写作成人教育班”，但“也没有真正学到东西”，只是经由这个管道接触到波士顿的喜剧圈，开始了他的表演活动。第一次登台是2002年，他说，“不太容易，因为人家不愿意提供机会”。他努力在笑话题材上不断创作，有时写100则笑话，只有一则好笑，最早的听众是黄西当会计师的太太金妍。他的移民题材，配上货真价实的外国口音，很快就受到欢迎，他打入2003年波士顿国际喜剧节的决赛。<br />
<br />
　　专门帮“雷特曼秀”发掘人才的布瑞尔(Eddie Brill)2005年在波士顿听了黄西的表演，认为他深具潜力，开始要培养他。他要黄西不时送给他表演的新材料；观察了三年后，2008年，布瑞尔再到波士顿看黄西表演后，觉得时机成熟，他让黄西设计一套能够拿出来的节目，为“大卫?雷特曼秀”试镜。<br />
<br />
　　因为与一般秀场不同，在全国性萤光幕上亮相必须精心设计。布瑞尔说，他以多年经验相信黄西终究会大红大紫。两人开始为在“雷特曼秀”节目演出的段子展开合作。黄西说，布瑞尔把他写的几则笑话做次序上的整理排列，更有节奏感，也指导了他一些讲笑话时的节奏感。<br />
<br />
　　4月17日的成功演出后，赖特曼非常欣赏，罕见地拉著乐得不知所措的黄西一起谢幕。布瑞尔认为黄西是自己近几年来所发掘到“最清新、最成功的演出”，非常得意。黄西在麻州的实验室同事大吃一惊之余，都跑来道贺，祝他即将名利双收。<br />
<br />
黄西-表演特点<br />
<br />
　　他的脱口秀完全不同于美国的黑人和白人的口水滔滔，而有另外一种喜剧的魅力---全部是最简单的字词，配合以木讷的表情和僵硬的动作，讲那种需要动脑筋才能理解的冷幽默。最别致的是，他是采取停顿和沉默来控制观众，让他们在这个间歇想明白笑话的意思，或者这种沉默无言本身也成为了表演的一部分。人们为了沉默而大笑，这是喜剧大师才有的能力（参考电影《月亮上的男人》前15分钟）。<br />
<br />
　　黄西<br />
<br />
　　他使用英文，讲的是美式笑话。我也曾考虑过放字幕版，但是他的笑话大部分都有语境上的背景。比如说他讲接受移民官考试的段子，第一个问题是：谁是本杰明.富兰克林？Wong回答说：难道他就是我们小区商店被抢的原因？第二个问题是：什么是宪法第二修正案？Wong再次回答说：难道它就是我们小区商店被抢的原因？<br />
<br />
　　现场的美国人爆笑不已，但是对于中国人来说，要理解这些笑话很难。第一条是因为本杰明.富兰克林的头像出现在美元上。第二条所说的宪法第二修正案，内容是保证了美国人民有持枪的权利。从Youtube的回帖上看，不单是中国人民理解有困难，就连美国人民自己都够呛，许多耿直的美国人留言提问说：这有什么好笑的？为什么这个傻逼连续两次用相同的答案回答不同的问题？<br />
<br />
　　笑话都需要注释的话，那就一点都不好笑了。即便配上字幕，要让屏幕前的人笑出来，字幕可能一秒内要闪过50个字，这就成为飞眼训练而非一种娱乐了。前来这里的读者已经算是教育程度很高的网友了，即便如此，根据历史统计，不通英文的读者依然在总访问人数的7成以上--这可以通过纯英文帖子和英文视频的点击数和留言数统计得出。<br />
<br />
黄西-表演过程<br />
<br />
　　黄西<br />
<br />
　　黄西站定后的第一句话是：“大家好，我是爱尔兰人。”全场笑翻，赖特曼也跟着大笑，黄西知道他的笑话题材已触动敏感的美国流行文化神经。<br />
<br />
　　黄西的表演片段在YouTube上的点阅数，两周以来已超过10万人次，留言评论褒多于贬，一致认为很好笑。大家开端打听这位戴着眼铐，满口中国腔英语的黄西究竟是谁？口音这么重，相貌又不出众的人，怎么能上全国电视节目，用英语讲美式笑话，还能让老美笑得东倒西歪？<br />
<br />
　　白天他是在实验室正经做研究的科学家，到晚上就摇身一变成演员，穿梭在新英格兰地区的酒吧、夜总会、俱乐部和大学礼堂，表演单口相声。<br />
<br />
　　黄西在吉大时就喜欢写笑话和讲笑话，马克吐温与导演伍迪雷。阿伦的作品对他的启发很大，让他了解美式幽默与美式笑话。<br />
<br />
　　到麻州任务后，他利用时间报名“笑话写作成教班”，经由这个管道接触到老板顿的喜剧圈开端表演，2002年第一次登台。他努力在笑话题材上不断创作，最早的听众是当会计师的太太金妍。他的移民题材配上货真价实的外国口音，很快就受到欢迎，打入 2003年老板顿国际喜剧节的决赛。<br />
<br />
黄西-精彩内容<br />
<br />
　　黄西<br />
<br />
　　段子之一：“我是新移民，当年来美国后，我开着一辆旧车，车后保险杆上贴了不少标语贴纸，我都不懂，撕也撕不下来，我开了两年之后，才知道其中一张是‘如果你不懂英语，滚回去’。”<br />
<br />
　　段子之二：“我好不容易才获取公民资格，我得去上美国历史课，去回答　谁是班杰明。富兰克林？我都只有啊…的份。去年，我儿子在美国出生，我抱着他，‘哇，你这小子一出生就已经是美国人了，我问你，你知道谁是富兰克林吗？’”<br />
<br />
　　段子之三：“现在我车上贴了‘车内有婴儿’的标语，这可算是一种恐吓，因为有一个大哭的婴儿和唠叨的老婆，我再也不怕死了。”<br />
<br />
　　擅用自己华人背景说笑，黄西在美国搞笑艺人圈内迅速走红。对此，在美国搞笑界闯荡26年，上遍 “国家广播电视星期五胜利之光”(NBC FridayNight Lights)与Comedy Central的华人搞笑艺人余智敏(ByronYee)，对黄西的抢眼表现直呼难得：“能上大卫？雷特曼脱口秀，是所有搞笑艺人的最高梦想之一。”<br />
<br />
　　话锋一转，余智敏表示，想在好莱坞搞笑圈生存极为困难，挤进去难、持续令人发噱也难。“像我们这种在美国土生土长的华人搞笑艺人，仍具全国知名度的，就我所知道，仅剩三人”。</div>

							
							
							
							
