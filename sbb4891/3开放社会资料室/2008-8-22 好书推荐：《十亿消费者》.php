													<h2>好书推荐：《十亿消费者》</h2>
						
						
																											<div id="postmessage_178" class="t_msgfont">生在网络时代是幸福的，这本书天朝不可能出版<br />
链接在此：<a href="http://www.yeeyan.com/articles/view/freeyiyu/4152" target="_blank">http://www.yeeyan.com/articles/view/freeyiyu/4152</a><br />
附上翻译者的博客，以及后记：<br />
<a href="http://zhouzq.spaces.live.com/blog/cns!91A333070012A8C7!1244.entry" target="_blank">http://zhouzq.spaces.live.com/bl ... 0012A8C7!1244.entry</a><br />
译者后记<br />
<br />
《十亿消费者》一书，我大概是在豆瓣上看到的，当时是我一个做记者的同学在看的书。有一次去香港出差，在机场看到这本书，就买了下来。07年中的时候，大致看完了第一遍。接近年底的时候，产生了翻译的念头，于是又读了一遍，确保没有太多难以理解的地方，就在07年12月24日打下了第一个字。<br />
<br />
<br />
最初翻译的动机，并非想要出版，因为读完第一遍，就知道这样一本书在国内几无出版的可能；更为纯粹的动机，其实就是想练练笔。一开始的时候，只是发在我自己的个人博客上，以一天两节的速度更新。有Messanger上的好友反馈说天天等着看我的翻译内容，算是一种激励，这样坚持了近一个月，差不多翻完了3 章。<br />
<br />
<br />
很偶然的机会，在和菜头的博客上看到了对译言的介绍，这时我已经有了让更多人看到本书翻译的念头（我个人博客流量非常小，只是几个朋友来看看而已）。于是就开始在译言上连载。这里要坦白当初的一个小聪明，由于当时已经翻完3章，如果一口气全贴出来，也许无法产生足够长的影响力，而且对我后期的持续更新也会带来很大的压力，因此选择了每天贴出一部分的策略。 必须承认效果还是不错的。这是在2008年1月20日。<br />
<br />
<br />
3 月份以后，工作增加，而且带孩子的责任也加大，所以无法保持最初的更新速度，中间有了很大的间断。期间也有出版社的人在网上找到我，问我是否可以出版，让我有些心痒，不过依然不报希望。但是这个时候，版权的问题越来越突出了。我在网上和雷聊天，他说他有一个朋友正好认识作者，希望引见。这正是我希望的，于是去见了原书作者Mcgregor先生。沟通很顺利，关于版权出版，大家也都认为这样一本书在大陆出版很难，关键不仅仅牵涉到一些敏感事件，而且其中一些人物依然活跃在领导层。不过作者口头上同意了我继续在自己的博客和译言上连载翻译，让我多少松了一口气――我原本带着一旦作者不同意，就立刻中止在网上继续发布翻译的打算去的。<br />
<br />
<br />
此后的翻译速度更慢了，中间一度停顿了很长时间，让很多人着急。这期间，有两个人对我产生了不小的触动。一个是“都是骗银地”，他是一名中国留美学者，在他的博客上推荐了我的翻译。而且他还提出给我汇30美元，作为对我翻译工作的肯定，让我非常意外，也非常惊喜（后来正碰到汶川地震，于是我委托他把这笔钱通过牛博网捐了出去）。另一个人是和菜头。其实，我最初的时候，就想过让他在自己的博客上推荐我的文章，但当时想得是翻完以后再与他联络，不过他还是在某天的博客上推荐了。这两个人的推荐，让我在译言的小组人数大为增加，越来越多的人开始读我的翻译文字，并且催促我尽快完成最后的一部分内容。<br />
<br />
<br />
中间还有不少读者在阅读过程中，找到了很多翻译和打字上的错误，并给我写信一一指出，让我非常感动。<br />
<br />
<br />
正是这些读者的推荐、期待和关注，让我觉得自己不能再懒了，于是鼓足后劲，终于在2008年7月21日翻完了最后一个字。<br />
<br />
<br />
下面来谈谈我对这本书的看法。<br />
<br />
<br />
对于本书的赞美之词，我不想多说，每个推荐此文的读者都已经说得很多了。作者有着在中国的丰富经历和广泛人脉，写出了很多不为大多数人所知的故事，而且凭借着华尔街日报记者的专业功底，其视角和洞察力独特而深刻，文笔老道而流畅。<br />
<br />
<br />
但是也正如作者在序言中所说的那样，本书只是“展示”了一副中国商界的画卷，但并非阐述和分析，虽然每章都有作者自己的观点，但大部分也只是对一些事实的列举，偶有惊人之语，却缺乏更为深入的剖析和理解――当然，这比中国的财经记者们写出来的文字，已经好上百倍。<br />
<br />
<br />
不过就此而把此书说成“奇书”，我觉得多少有一些夸张。这本书的英文原版，我读了三遍，前两遍是在开始翻译前，最后一遍是在翻译开始后，每次翻译前，再把计划翻译的内容以及前后文过一遍。第一遍读的时候，我也很是兴奋和激动，颇有一种“拨开云雾见日月”的感觉。但是读到第二遍，直至第三遍，这种感觉就大打折扣。回想自己最初的感受，其实是一种“写得真对”的共鸣而已，再深究下去，就是“我也是如此认为，现在终于有人证实了我的想法”的感受。<br />
<br />
 <br />
有一段时间，我特别在乎外国人对中国的看法，上网经常会找一些“老外看中国”这样的文章或视频。究其根本的心理，其实不过是为了从他人那里得到一种认同感 ――认同我对这个国家的看法，包括好的看法和坏的看法。我不知道别人是否有过这样的经历，我自己曾经有过，一见到老外，“哈罗”之后就是问他觉得中国怎么样。他说好，那我高兴。他说某些地方不好，如果也正是我所认为的不好的地方，于是就和他一起批评中国，非常来劲。<br />
<br />
<br />
为什么会这样？<br />
<br />
<br />
当然有自己的想法想要寻找知音的感觉，但是也多少带有一些不自信的因素在里面。<br />
<br />
<br />
回 到这本书，《十亿消费者》的确说出了很多当前中国社会的问题，包括缺乏核心价值理念，缺乏安全感，商业活动中的灰色地带，政府对市场的非正常干预等等等等。但其实书中所说的这些问题，如果是一个有心人，会观察国家大事和周围的人与事并能独立思考的人，都有所体会到的。这点在读者留言中也看得出来，很多诸如“说得太对了”这样得表述，其实就是“余心亦有戚戚焉”。但是对这些问题的剖析，找到根本原因所在，以及更为重要的，如何去解决这些问题，这些内容本书涉及很少，有些则根本没有提及。<br />
<br />
<br />
当然，《十亿消费者》已经揭示了问题，再要求作者去找到问题的原因甚至提出解决之道，也许是要求过高的。但退一步来说，对于书中的这些问题，政府和社会看到了哪些、正在采取哪样的步骤去解决它们、以及取得了如何的进展或挫折及其原因分析，这些内容也并 未涉及，不能不说是本书的一大遗憾。<br />
<br />
<br />
此外，本书的结尾略显潦草。无论是SOHO，还是娃哈哈，还是联想的故事，不说分析，即便是讲故事本身，相比前几章也显得不够精彩，内容不够充实，有点仓促结尾的感觉。<br />
最后谈一下翻译本身。<br />
<br />
<br />
我中学在外语学校度过，对于语言学习已经深恶痛绝，所以在报考大学的时候，任何语种的外语专业一律不考虑。但是也不得不承认，中学六年对如何学习一门外语的近乎残酷的训练，已经在我的骨子里刻下了无法抹去的烙印。这种近乎基因的影响让我在后来的工作和生活中，对语言的学习有了一种本能的反应。<br />
<br />
<br />
从大学毕业到现在，我的工作总是和翻译脱不了关系。在国家机关替领导翻译，在公司参加谈判的翻译，现在依然有大量的翻译工作要做。既然这是我的工作一部分，那么就要做好，如何做好？“把需要变成爱好”。我自己觉得，在这点上我做得还不错。<br />
<br />
<br />
翻译是一个苦活。要能够长期坚持，必须对翻译有着很强烈的热爱。我希望在译言的每位译者都能在翻译中找到自己的乐趣，更上层楼。<br />
<br />
<br />
最后再次感谢所有关注《十亿消费者》翻译工作的读者，谢谢你们的支持和肯定，让我能够坚持把这本书翻完。也感谢我的太太，因为她的耐心和理解，对我们的小女儿的无微不至的关心和照顾，让我能够有精力和时间做这件有意义的工作。<br />
