作者 chinese&nbsp; &nbsp; &nbsp; &nbsp;&nbsp; &nbsp; <br />
2010/01/14, Thursday<br />
听众朋友大家好，您现在收听的是伊朗华语台为大家播送的听众信箱节目，在今天这一时间的节目里，我们将为大家播送部分听友来信，并就其中提出的问题作出回答。好下面请听今天的节目内容。<br />
<br />
中国北京听友赵欣给本台发来邮件说，我是一名学习外语的大学生，由于专业的原因经常比较关注国际时事，因此自然也就成了伊朗华语台的一名听众，当然我是通过互联网收听的，有时候太忙了只能匆匆上网浏览一下新闻。周末有时间了多看一些内容。经过了解我也逐步的喜欢上了伊朗，对伊朗的新闻也比较关注。我还打算学习波斯语呢。听说波斯语非常美，尤其是波斯语诗歌读起来更是令人陶醉。中伊两国也是友邦，两国之间的关系可追溯到2000年以前，而古丝绸之路更是连接中伊两国之间的纽带。将来有时间可能的话，我真想骑着骆驼穿越西部戈壁，再此踏上我们的先辈们曾经走过足迹，领略西域沙漠的风光。听说中国有好多东西都来自古波斯。比如，胡琴、胡萝卜、胡椒粉、孜然、杜塔尔、斯塔尔、狮子等。这些充分说明中伊两国之间过去一直保持着非常密切的交往。 但是我观察，近年来虽然中伊两国之间的年贸易额在逐年增长，可是我发现了一个问题，那就是近年来随着中国经济不断增长，国力不断强大，中国领导人每年到外国访问的次数也比较频繁，尤其是访问那些蕴藏有丰富的自然资源和石油天然气的国家。可令我不可理解的是，近几年来根本没有听说中国哪位高级领导人访问伊朗，或是邀请伊朗领导人来访问中国？这是为什么？说两国关系不好吧，两国的年贸易额在逐年大幅度增长，伊朗还是中国的第三大原油供应国，同时也没听说中伊两国之间有什么矛盾？说两国关系好吧，两国元首几年间不进行互访，这完全不利于发展两国关系。两国要想进一步发展和巩固两国关系，离开两国最高领导人的互访与沟通和支持是不可能的。还是顾及到伊美关系和中美关系的原因？<br />
<br />
第二这几年尤其是世界经济危机爆发之后，中国成了世界经济中的一枝独秀，20000亿美元的外汇储备，即让中国领导人和人民感到自豪和满怀信心，同时也让负责金融事务的领导们感到压力，他们担心如何来花这么一笔巨资。所以，我们经常会听到中国政府向不同国家提供巨额贷款，举例来说中国去年先后向俄罗斯提供250亿美元，向巴西提供120亿美元，向委内瑞拉提供150亿美元贷款，向其他发展中国家提供的贷款就不胜枚举了。可是我始终没有听说中国向伊朗提供贷款的消息。我不知道是伊朗政府钱多不需要中国的贷款呢？还是中国政府听信西方媒体经常传出的“美国或以色列要军事袭击伊朗”的传闻而不敢贷款给伊朗呢？还是中国奉行着与美国等西方国家相同的政策，有意的在制裁伊朗呢？当然，钱两点还可以理解，说中国与西方一道配合西方制裁伊朗的政策，这有点说不过去，因为乘西方与伊朗交恶之机，中资公司大举进入伊朗这个中东地区最大的市场不是对中国有利吗？<br />
<br />
&nbsp; &nbsp;第三：以改变口号上台的美国总统奥巴马上台之后，一直高呼要改变和伊朗关系的关系，然而伊朗为什么没有相应和美国改善关系呢？ 而是一直采取和美国对抗到底的政策呢？<br />
<br />
&nbsp; &nbsp;最后一个问题更有意思，最近几天中国互联网上传说，中国最大的搜索引擎――百度网站被伊朗网军给黑了。有一些人则声称要报复，号召中国黑客黑掉伊朗的部分政府网站。在此，我也不理解的是中伊两国之间不存在任何矛盾和分歧，伊朗网军干吗没事找事，与中国的百度网过不去呢？ 我希望华语台能就以上问题作出回答，谈谈你们的看法，不胜感谢。最后别忘了寄波斯语教程和明信片给我。<br />
<br />
感谢北京听友赵欣关注伊朗华语台的节目，并给本台来信和提出问题。关于您的来信中提出的以上三个问题我们做出简单的回答。<br />
<br />
第一：正如您所说的中伊两国关系源远流长，在古时候中伊之间的关系密切，两国民间交往也比较频繁。早在伊斯兰教之前，佛教传入中国之后，许多著名的佛经还是伊朗的学者们从印度的梵文翻译成汉语的。后来在伊斯兰教进入伊朗之后，伊朗人不断发扬光大了伊斯兰文化与思想，还将伊斯兰教传到了中国，许多年来波斯语在中国一直是穆斯林的宗教文化语言。今天中国穆斯林的许多宗教属于还依然使用的是波斯语。这些足以证明中伊两国之间过去保持着非常密切的关系。在人类进入新的千年之后，中意两国之间的关系也得到了长足的发展，两国之间近年来的年贸易额在逐年递增，在政治领域，中国是上海合作组织的重要成员国，而伊朗则是该组织的观察员国，两国在许多国际重大问题上持相同或相近的立场，并展开着良好的合作。但是令人遗憾的是，中伊两国之间在政治和经贸领域的合作潜力巨大，两国之间似乎因一些人为的障碍并没有达到应该达到的高度。我们衷心地希望中伊两国领导人能够放眼未来，高瞻远瞩，用高度战略眼光来看待发展中伊两国友好关系。为此两国高层领导人应该本着增进了解、友谊与合作的精神进一步加强联系与沟通，以免被西方的反伊和反华势力有机可乘，在伊中两国之间制造矛盾和挑拨离间。<br />
<br />
关于第二个问题，伊朗伊斯兰共和国是一个油气大国，伊朗具有这良好的还款信誉和能力。同时伊朗政府也制订了宏伟的经济发展计划，因此，伊朗需要大量的国外贷款，以完成国内的基础建设项目，推动伊朗的经济快速发展和提高。我们相信中伊两国领导人一定会利用他们的智慧，尽快解决两国在经贸领域存在的一些障碍，为中伊两国之间展开密切的经济合作铺平道路。伊朗是一个独立自主的国家，一直奉行着独立的外交政策。而中国则是当今世界的一个经济大国，放眼世界，今天只有中国有能力向其他国家提供巨额贷款，这为中国公司提供了千载难逢的契机。他们终于可以扬眉吐气在国外大展宏图，大展身手。<br />
<br />
关于第三个问题，大家都知道，其实美国并不是一个真正的民主国家，民主只是糊弄美国人民的虚假口号。其实美国的体制属于寡头政治。该国完全被控制在军火商和石油商等金融寡头的手中。由于美国的布什政府奉行单边主义和军国主义霸权政策，因此遭到了世界人民的反感和憎恶。为了使美国摆脱困境和改善美国在世人心目中的形象，因此美国的金融寡头们将该国政之舞台上的那张白脸换成了黑脸，而角色和内容依然如故。因此，奥巴马所高呼的改变口号只是欺世盗名，欺骗美国人民和世界人民的虚假口号而已，跟本没有什么实际内容。美国的好战政策依然如故，美国的中东政策，美国的对伊朗政策，美国的对华政策等等全部依然如故，甚至与前任小布什相比真是有过之而无不及。比如近来美国宣布将向台湾出售爱国者3型导弹在内的价值65亿美元军火就说明了这一问题。鉴于此，我们看到美国的什么政策改变了呢？对于伊朗，奥巴马企图欺骗伊朗，以一句改变口号来欺骗美国小布什政府用武力威胁都无法实现的目标。在伊朗大选之后我们看到，美国为了实现自己不可告人的非法目的，真是不择手段，在发动颜色革命失败之后，又煽动伊朗的一些骚乱分子企图制造内乱，在这一阴谋遭到失败之后，我们看到最近几天美国又指示恐怖分子开始在伊朗制造暗杀伊朗科学家事件。但是，美国的这些努力都注定将遭到失败。<br />
<br />
第四，今天西方一些邪恶势力为了实现自己的地区霸权和全球霸权目标，已经到了穷凶极恶丧心病狂的地步。以色列情报机构及其盟国的情报机构联手，通过各种手段企图在伊朗和中国之间制造矛盾和不信任感。他们制造一些假新闻，在伊朗国内煽动反华情绪，在中国煽动反伊朗情绪。更为甚者的是他们居然制造谣言和虚假消息，声称伊朗支持疆独。但事实上，伊朗根本没有支持疆独势力。大家都知道，在去年发生的7.5事件中是美国在支持疆独分子。同时美国还支持着反对伊朗政府和在伊朗频繁制造恐怖事件的真主军恐怖组织。凡是能上网的人到真主军的网站上看一下就会知道，该恐怖组织是如何辱骂伊朗政府和中国政府的。而该恐怖组织得到多个西方国家情报机构的支持。关于是否是伊朗网军破坏的中国百度网站，事实大于雄辩，中国网民的眼睛是雪亮的。据环球网报道，软硬件都号称强大的百度12日晨在黑客袭击下一触即溃，短时间内影响了大面积国内外客户。<br />
<br />
 　百度遇袭，直接受到影响的是数不清的网络客户，他们对此作出的反应或议论也相当之多：或者猜测是谁所为，或者谴责袭击行为，或者为中国的网络安全担忧。环球网当天就此发起了一组在线调查。截至22时，在第一项调查中，有68.9%（2231票）的人认为「西方某国是幕后黑手」，只有5.7%（184票）的人认为是「伊朗黑客所为」，其余25.4%的人选择了「说不清」。中国前驻伊朗大使华黎明在接受采访时也表示，「虽然中国和伊朗的关系因为核问题有一些变化，但也不至于出现这类事件。」<br />
