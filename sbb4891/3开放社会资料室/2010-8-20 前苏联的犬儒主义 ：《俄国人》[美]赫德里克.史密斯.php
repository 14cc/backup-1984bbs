我为啥要推荐这个书？大家知道的自己明白，做一个不犬儒不猥琐的人，我们才有更好的未来。选择读什么书是很重要的事情。<br />
<br />
<br />
作者：HP<br />
<br />
美国记者赫德里克・史密斯（Hedrick Smith），在他那本写于1976年的书中，向读者讲述了勃烈日涅夫时代弥漫于苏联社会的犬儒主义。<br />
<br />
史密斯发现，在苏联，实际上已经很少有人相信共产主义。<br />
<br />
首先，苏共领导人自己就不相信。史密斯引用一位莫斯科的科学家的话，&quot;我们的领导人，把共产主义看作一种象征，用来判断其它人是否忠诚。&quot;（勃烈日 涅夫的侄女柳芭发表回忆录，其中写到，勃烈日涅夫当年曾对自己的弟弟说：&quot;什么共产主义，这都是哄哄老百姓听的空话。&quot;）<br />
<br />
一位高级编辑说，当政的这些苏共领导人是没有信仰的人，&quot;是一些对一切都无所谓的人。他们所要的是权力，纯粹是权力&quot;。<br />
<br />
这位编辑还说，社会上的几乎所有人，都不再相信官方的意识形态，而且对各种事情也并非没有自己不同的看法。但是一到正式场合，他们却照旧举手拍掌， 重复着官方的陈词滥调。人们明知这一切是毫无意义的，是逢场作戏，&quot;可是你必须去玩它&quot;。<br />
<br />
史密斯认识一个苏共少壮派官员。看上去此人是矛盾的复合体。一方面，在和朋友谈话中，他批评时政，攻击腐败，俨然是个改革家；另一方面，他又对本国 的政治感到自豪，为自己能身处权势集团而踌躇满志。他清楚地知道斯大林时代的恐怖，也不愿意再回到那个时代，但与此同时，他又对斯大林靠强权建立起一个庞 大的红色帝国而十分骄傲。一方面，他很乐意向别人显示他的思想解放，根本不相信官方的教条。另一方面，他又对自己善于掩盖个人观点，在党内会议上以善于发 言著称而得意。<br />
<br />
其实，这正是苏共新一代官员的一种典型----无信仰的、犬儒式的机会主义者。&quot;可见，&quot;史密斯总结道，&quot;个人只要服从听话，不公开向意识形态挑 战，不管信也好不信也好，都不是关键问题。&quot;<br />
<br />
由于正式场合都是逢场作戏，社会看上去毫无改变的希望，许多俄国人就产生了看破红尘的态度，物质主义开始泛滥。人们这样想：&quot;人只活一世，而这一世 是短促的。所以多享受一点生活吧!&quot;<br />
<br />
物质主义的泛滥进一步冲掉了残存的理想主义。许多人为了一点点物质利益----为了一次出国机会，为了分得一套房子或搞到一部新汽车----甘愿放 弃自己的独立政见。这样，当局无须再采取大规模的恐怖措施，就足以控制住它治下的广大人民。<br />
<br />
少数勇敢的持不同政见者，依然在发出他们的声音。起初，他们赢得了广泛的尊敬，虽然敢于公开表示这种尊敬的人不多。然而令人惊异的是，到后来，当局 对异议人士泼污水，很多人竟然也认同政府的行为。<br />
<br />
史密斯对此大惑不解。一位名叫瓦连京・图尔钦的异议人士对他解释说：<br />
<br />
&quot;第一，诚实的人使得那些沉默的人，由于没有大胆说话而有负罪感。因而，他们感到不得不攻击前者，使得前者看上去不那么高尚，这样才能使自己显得不 那么堕落。&quot;<br />
<br />
&quot;第二，根据他们自己的经验，他们觉得每一个地方的每一个人，都是在演戏。他们好像妓女一样，因为自己是妓女，便认为所有的女人都是妓女。他们认为 根本不存在真正的诚实，根本没有人真正追求真理。那些异议人士，很可能就是一些撒谎的骗子。&quot;<br />
<br />
&quot;这种犬儒主义给当局帮了大忙。由于人们普遍互不信任，利用这一点，就能把不听话的异议人士排斥于社会之外。虽然人们可以到西方去旅行和收听西方的 电台。但只要普遍存在着这种犬儒主义，他们就会认为那不过是另一派的欺骗性宣传，所以也就不必当真了。这种犬儒主义提供了极权国家今天的稳定，以代替斯大 林时期的大规模恐怖。&quot;<br />
<br />
