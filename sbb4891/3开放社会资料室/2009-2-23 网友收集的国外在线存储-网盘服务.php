从我的google reader上及平时上网收集到的在线存储-网盘服务，我收藏了一大堆。我就把他们全部写在这篇日志里，并且持续更新。如有勘误，欢迎在评论中指正。<br />
1.Box.net<br />
<br />
文件共享网站box.net提供1GB的自由空间，免费帐户文件最大限10MB…<br />
<br />
2.Drop.io<br />
<br />
提供预览图片和PDF格式等文件的功能，最近已增加了几个新功能，例如免费发送接收传真和语音录音。免费的帐户有100MB的储存空间限制…<br />
<br />
3.MediaFire.com<br />
<br />
无限大的存储空间,上传大文件最大限100MB…<br />
<br />
4.Windows Live SkyDrive<br />
<br />
用skydrive需要你已经拥有Windows Live帐户，个人上传文件大小限50M,总容量5GB…<br />
<br />
5.FolderShare.com<br />
<br />
跨平台的FolderShare，是一个胜于传统文件共享服务的文件同步工具。（事实上，它就是利用firefox）FolderShare爱好者强调很容易将桌面上的一个文件夹共享，然后迅速同步到朋友的电脑上。所有您需要做的就是设置，然后任何拖动到共享文件夹里的文件就会自动复制到你朋友的计算机（事先设置好了共享）上。<br />
<br />
6.YouSendIt.com<br />
<br />
该网站允许你直接将最高 2GB 的文件通过电子邮件分发给其他人…<br />
<br />
7.Arkiva.com：在线数字文件存储<br />
<br />
Arkiva 是免费的在线文件存储服务，主攻于数字文件的存储，如video、movie、photo等等，并提供在线文件转换功能，可将你的数字文件转录了DVD…<br />
<br />
8.getdropbox.com<br />
<br />
暂时在测期间，getdropbox.com为每位用户提供了2GB的免费存储空间，内测结束后，将会为免费用户提供1GB空间，但不会收回内测用户的空间，到时还会推出付费帐户…<br />
