吴欢：李沛瑶被杀真相 <br />
<a href="http://changhelanghua.blog.sohu.com/144655837.html" target="_blank">http://changhelanghua.blog.sohu.com/144655837.html</a><br />
<br />
<br />
1996年2月2日，全国人大副委员长李沛瑶在自己家中被人杀害。2月3日公安部副部长白景富在会上介绍说，罪犯是一位名叫张金龙的武警战士，1994年12月入伍，1995年3月开始到李沛瑶住所值勤。<br />
张金龙入伍前就有多次偷盗行为，2月2日四时许，他利用值勤之机，潜入李家行窃，将李沛瑶惊醒，遭到李沛瑶严厉申斥和教育。张见事已败露，速从李家厨房拿出两把菜刀行凶。李沛瑶与之英勇搏斗，并夺过菜刀自卫，但终被杀害。白景富特别指出:&quot;这是一起没有政治目的和其他背景的案件，罪犯的作案动机是谋财害命。&quot;<br />
事过八年，今年5月27日人民政协报又发表《李沛瑶遇害以后》，重谈旧调。 我跟李沛瑶相熟，忘不掉他那张生动的笑脸，但案发后我却发现这起案件迷雾重重。<br />
一、张金龙既然负责李沛瑶住所的保卫工作，何时不能监守自盗，何必等李沛瑶在家时才下手？<br />
二、既然将李惊醒，迅速出逃就是了，为何还要面对&quot;李沛瑶严厉申斥和教育&quot;？以副委员长的能力和口才，为何安抚不了一个小战士？<br />
三、张金龙20来岁，被选来给副委员长看家护院，自然身手不凡，他若想杀害一个63岁的老头子，不过举手之劳，何必再去厨房拿两把菜刀？<br />
四、要杀人，一把菜刀足矣，何苦拿来两把？让李沛瑶表演&quot;空手夺刀&quot;吗？<br />
五、两个人的力量对比相去云泥，李沛瑶居然从张手里夺过一把菜刀，除非他是武林高手；但我知道他会跳舞，从未听说他会武术。<br />
六、既然是&quot;谋财害命&quot;，致人死命可矣，何必把对方捅成马蜂窝（多少刀已经记不清了，依稀记得是20几刀）？没有深仇大恨，可能吗？<br />
七、既然杀人的目的是掩盖自己，张金龙何必又投案自首？<br />
八、既然是谋财害命，部长大人何必特别强调&quot;没有政治目的和其他背景&quot;，这不是此地无银三百两吗？……<br />
种种疑问表明，要么张金龙有精神障碍，要么整个案件的情节全是编造。但从张金龙被迅速处决来看，他精神正常。那么真相究竟是什么？过了没多久，我果然获得了被掩盖的真相。原来李沛瑶夫妻感情失和，长期分居，张金龙见他一个人很可怜，就把自己的妹妹从农村叫来给他做保姆。没想到李沛瑶强奸了她，致其怀孕后又将其赶走。面对耻辱，张金龙只有两种选择：一是打掉牙齿往肚里咽，含垢忍辱，继续为委员长看家护院，见了委员长照样笑脸迎送，打躬作揖；二是铤而走险，以暴易暴，以鲜血浇灭胸中怒火。可惜性格暴烈的张金龙选择了后者。也许他看《水浒》之类的旧小说太多，太羡慕那些杀人不眨眼的英雄好汉了吧？ 道德家会谴责说，张金龙为何不拿起法律的武器？其实，这是连道德家自己也不相信的鬼话。因为谁都知道，我们的法律虽然汗牛充栋，却不过是权贵的妾妇，柔媚无骨，委琐下流。<br />
受命去惩办弱者时，它雷霆万钧，无坚不摧；需要保护弱者时，它往往装聋作哑，推而不动，乃至隐迹遁形。这些年来蒙冤受屈者数不胜数，小到普通百姓，大到国家主席；少则十年八年，多则几十年不得昭雪。一位小战士和一位农村小姑娘企图以法律来对抗副委员长，无异于孤豚咋虎，胜负不卜而知。可悲可叹的是，对于中国的老百姓来说，虽然50年前就被宣布为国家的主人，但在权贵眼里，他们始终是路边的狗尾巴草，猪可以吃，羊可以啃，牛可以踩，狗可以尿；他们没有自由，没有尊严。奸污女秘书、机要员、女护士的恶习已久，奸污小保姆更是小菜一碟。农民一夜醒来，发现自己的土地已经被贪官们低价卖掉，只好到城市流浪；城市居民正在梦中，推土机已经来到自己门前。对于这些，因为没有丝毫的反抗能力，人们只能默默地忍受；最激烈的反抗，也不过是把汽油淋在自己身上点燃。张金龙奋然一击，仅如流星一闪，随即便消失在茫茫太空。时光照样流驶，街市依旧太平。<br />
8年来，每当回忆起这件案子，我都感到不由自主的愤怒：为什么在这个&quot;太平世界&quot;里，大人物的面子比小人物的尊严更重要？大人物人面兽心，因惹火烧身而死后，还要把他的&quot;兽心&quot; 挖空心思掩盖起来，并精心为他整修出一张正人君子的面孔；小人物受尽屈辱，死后还要在他的身上泼满污水。张金龙为报仇雪耻而行凶，主动以自己年轻的生命来偿债，当局居然忍心污蔑他是小偷；李沛瑶因奸被杀，官府竟为他编造出一个与盗窃犯英勇搏斗而牺牲的故事。最可怜的是张金龙的父母，女儿被糟蹋，儿子被枪毙，还要为儿子背上盗窃犯、杀人犯双重罪名，从此只能在含垢忍辱中消磨残年，不只何时才是尽头；奇怪的是，8年之后仍有人往他们滴血的伤口上撒盐。<br />
