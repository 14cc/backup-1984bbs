4、洪水<br />
我访问了一些老年妇女，她们中的很多人，第一次接触这个话题。有位老人说，她可从来没有看过她的阴道，也从来没有体验过性高潮。在她72岁时，她去接受心理治疗；在医师的鼓励下，一天下午，她独自在家，点上蜡烛，洗了个澡；接着，她打开音响，放出音乐，开始研究自己的阴道。她说，她花了一个多小时来干这件事，因为她有关节炎，腿脚不好。最后，当她终于发现自己的*时，她，哭了。下面这个独白就是献给她的。<br />
洪水<br />
下面那儿吗？哎哟，我的天啊，有多少年我都没再去过下面那儿了（以下扩号内文字为表演提示：冷漠的，隔膜的）。那儿流血、发炎、很难闻，还有霉菌。哦，那儿的腥味，连你自己都讨厌。<br />
不，不是说我天生不正常，没那么戏剧性。我的意思是说（教训口吻），嗯，像你这么一个聪明的女孩儿，怎么到处找人谈下面的事呢？什么，“阴……道”，这个词你不能说（苦口婆心的）！不错，它是我身体的一部分，可是你知道它让我倒了多大的霉吗（老道世故，因而语调平静）？那好吧，我就从初恋说起（平静，也很热心讲故事）。<br />
那个小伙子，他叫安迪。对，他很可爱（很得意、喜悦）；我们很般配。那一天，他邀我出去，就在他的车子里（很想讲，但是觉得讲不出口，不雅观，所以欲言又止）……<br />
我不能跟你说这个！说下面的事情。我没法说（这几句都很坚决，压抑自己，下决心守口如瓶）。（转教训口吻）你买楼，看楼盘就行了，你还真去看下水道？下面那儿，它跟下水道没什么两样，它每个月都漏水，还得把它堵住。这你都知道，（口吻转体贴，依然有长辈对孩子说话的口气）你们这些在外面跑的女孩子，倒霉时多不方便。<br />
哦，对，安迪，安迪很帅，他是我的初恋情人（得意，喜悦，夸张一点）。在他那辆白色跑车里（非常喜欢回忆自己开始初恋的浪漫情怀，但是找不到词语来说当时的尴尬，所以下面有点语无伦次），我，我当时在想……他忽然吻我，就像电影里演的那样，霸王硬上弓（惊惶失措状）。我真的没想到（很无辜的、很想解释清楚），我第一次体会到那种热情（像小女生那样）。就在他搂着我的时候，下面那儿，一股热流涌出我的身体（这几句话都说得软绵绵地，表现出难以控制）。那肯定不是尿，可它有点味儿（很委屈，很无辜）――老实说，我并没有真的闻到什么味儿（这一句是抵制上一句的，是老年人对经验的认识）；（回到当时的情境）但是，安迪却说，你怎么像……馊了的牛奶（夸张一点，做动作）。我想解释（无辜女生），我努力用衣服擦他那车座（非常认真地在椅子上磨屁股），可是，我堵不住自己（急得要哭了，很窘的样子）。（小女生状，仿佛是第一件新衣服，少女纯真的想象被身体的欲望和她对欲望的无知打破了。所有的幻灭和无奈、委屈在这里说出来。还有很多惋惜，因为为了这个失败的恋爱，新衣服也糟蹋了，真的不值得）那是一件新衣服，上面有淡黄色的花朵；在车座上擦过以后，再也没法穿了。安迪把我送回家，（叹口气，说明结局，不回避，夸张一点）我们，就这么吹了。<br />
（语调夸张）后来啊，我也和别的男人约会过，但是，一想到下面那儿，我就坐不住（紧张）。我在约会前就开始紧张，我从来都不主动（没办法的样子）。慢慢地，男人，对我也就没兴趣了（这几句，说得很无奈，好像说别人的事情一样）。<br />
哦，我做过一些梦（精神提起来，好像变了一个人，热情而奔放，释放出本性），疯狂的梦。哦，那很傻（甜甜地笑）。我总梦见一个像盖博（盖博是四十、五十年代好莱坞明星，演《乱世佳人》里的男主角，又高又大，女人偶像）的男人（好像回到初恋情怀）。他在门口等我，捧着那么大一捧玫瑰花（优美、神往）。我们去大酒店，吃大虾，喝红酒（沉浸、得意，说平生最得意的事情）。突然，这个盖博就把我揽到怀里，凝视着我的眼睛（满怀热情、奔放）……就在他要吻我的一刹那（做作的甜蜜），房子开始摇动（屏住气）；鸽子，有鸽子从桌子下面飞出来（惊喜，但惊喜里有惶恐；好像诺亚方舟的鸽子，和和平、也和洪水联系在一起，有好的意思，也有灾难的意思），我不知道为什么会有鸽子在那儿（紧张，这是爆发、达到高潮前的紧张），而那股热流一下子涌出来（释然，但也面临不好的结局）。我站在水里动不了（非常无力的感觉），而桌子、椅子都漂起来。盖博松开我，鱼从他的衣服里游出来；盖博站在齐腰深的水里，冷冷地说，你怎么是这样的女人。<br />
