2、阴毛<br />
（演员三人，一人独白，两人在后面做哑剧表演）<br />
除非你喜爱阴毛，否则你无法喜爱阴道。很多人不喜欢阴毛。我的第一位也是唯一的丈夫，他就讨厌阴毛。他说：“阴毛又脏又乱！” 他要我把它剃掉。剃完之后，我的阴道看起来膨胀、暴露，像一个小女孩。这个样子令他格外兴奋。当他跟我做爱时，我的阴道就像被刺扎着。我摩擦它感觉好点，但很疼；就像抓挠被蚊子咬过的地方。我觉得阴道疼得火辣辣的，那儿有刺眼的红色肿块，我再也不能忍受了。我拒绝再剃阴毛。不久，我的丈夫就有了外遇。<br />
