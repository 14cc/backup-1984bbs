乙：在座的观众，你们也许很担心，担心我们怎么表演这出戏。<br />
甲：我也很担心，我担心的是：为什么人们对这出戏心存疑虑。我担心人们如何说“阴道”这个词，我更担心人们不说这个词。我担心我自己的阴道。在我还是小女孩时，我曾被人强奸；成年以后，我很少对人说起这件事，我的身体和精神曾经是相互分离的。我从来没有想过，在自己两腿之间的这个部位，它值得我骄傲，它可以让我感到强大和生命力。<br />
乙：人们不去说的事物，它就不被看见、承认和记忆。我们不说的东西成为一种秘密，而秘密则导致羞耻、恐惧和神秘。我们说出这个词，是希望有一天我们可以自在地说，不再觉得羞耻。<br />
甲：随着更多的女人说出这个词，坦坦荡荡地说；说出它就不再是什么了不得的事。它成为我们语言的一部分、我们生活的一部分。我们的阴道变得完整、可敬和神圣。它与我们思维相连，点燃我们的精神。耻辱消失，暴力终止；因为阴道是真实的，是可以理解的；它与强大、智慧、敢于谈论阴道的女人相联系。<br />
乙：而这儿就是起点。这儿是我们开始思考阴道的地方，是去了解其他女人阴道故事的地方。<br />
