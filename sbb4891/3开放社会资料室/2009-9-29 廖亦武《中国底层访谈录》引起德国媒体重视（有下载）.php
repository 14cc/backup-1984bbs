																											<div id="postmessage_263172" class="t_msgfont">德语媒体 | 2009.09.28 <br />
<br />
廖亦武及其作品引起德国媒体重视<br />
<br />
<img src="http://i34.tinypic.com/14wybrc.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
中国当局禁止诗人和作家廖亦武出境参加柏林&quot;世界文化之家&quot;的讨论会及法兰克福书展前的作品朗读会，德语媒体纷纷谴责这一&quot;侵犯公民言论和旅行自由&quot;的做法，这些媒体分别介绍了廖亦武及其作品，给予高度评价。<br />
<br />
<br />
《南德意志报》介绍了廖亦武及其最近译成德文的作品：<br />
<br />
&quot;廖亦武是最具独创精神的中国现代文学作家之一。他的作品《中国底层访谈录》刚刚由菲舍尔出版社出版，书中收集了这位50岁的作家多年来与中国社会各种'弱势群体'三百多次访谈中的一部分。其中有厕所清洁工、妓女和街头艺术家，迈入老年的前红卫兵和前政治犯。廖亦武说：'我喜欢游走于草根之间，我认为，这些人的故事值得记录下来'。<br />
<br />
作者自己就曾是一名政治犯。1989年6月4日晚上，当中国政府命令坦克在北京碾压本国人民时，廖亦武写下了《屠杀》一诗。他说：'我知道，这首诗永远不会印刷出版，所以我公开朗诵'。这一小小的、但十分勇敢的抗议经过影像传到了加拿大。廖亦武被逮捕，从1990年到1994年，他在重庆坐了四年监狱。他'蹲在牢房的便桶旁，捉内衣上的虱子'。有一次，他的双臂被反铐在背上，长达23天。他两次试图自杀，却受到嘲笑。今天，他辛辣地说，监狱生涯使他受到了完整的教育。&quot;<br />
<br />
《日报》认为，&quot;无论与过去一段时间来自中国的大多数作品或与现在法兰克福书展主宾国中国的大多数作品相比，廖亦武这部作品都更为激动人心&quot;：<br />
<br />
&quot;廖亦武这部出色作品中的主角不同于西方仍然广为流传的整齐划一的老一套中国形象。尽管这些主角承受各种非议，但他们有判断能力、自我意识、思维敏捷，他们以极大的愤慨讲述了他们个人的小故事，使中国过去所宣扬的平等与社会公正以及现在所宣扬的让所有人共同富裕和享有更多的自由等神话不攻自破 。<br />
<br />
口述历史，也就是一种以谈话记录为基础的报告文学，目前在中国方兴未艾。许多中国作家、电影工作者和记者越来越想清楚地了解那些历史书中没有提及的事件，以及过去几十年人们真正经历的事情。但这样的回忆文化， 这样一种体裁类别，中国至今没有一个称呼上的概念。而廖亦武就是那些最激进、最热情、最卓越的考古学家和档案学家中的一员。&quot;<br />
<br />
《柏林日报》也回顾了廖亦武因《屠杀》一诗身陷牢狱的经过。对廖亦武被禁出国一事，该报写道：<br />
<br />
&quot;他虽然持有护照及正式邀请函，按照宪法他应享有言论自由和旅行自由，中国作为主宾国参加法兰克福书展是一个关系国家声誉的项目，但是如果党担心自己的名声或所谓'国家稳定'会受到损害，法律就一文不值了。当局常用'维护国家稳定'的说法为违反宪法的行为进行辩解。&quot;<br />
