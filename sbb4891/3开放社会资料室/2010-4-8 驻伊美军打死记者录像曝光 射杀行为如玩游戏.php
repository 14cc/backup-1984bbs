<br />
美国驻伊拉克军队一段秘密视频5日曝光，显示美军阿帕奇直升机2007年7月在巴格达向手无寸铁者开火，致使路透社一名摄影记者及其司机死亡、两名儿童受伤。<br />
<br />
网站公开加密视频<br />
<br />
一个标榜旨在打击政府与企业腐败的网站“维基解密”5日在华盛顿召开新闻发布会，公开了这段视频内容。<br />
<br />
“维基解密”自称经由美军渠道获得这段视频。视频原本加密，后被网站解密。<br />
<br />
视频拍摄日期为2007年7月12日，正是路透社伊拉克籍摄影记者纳米尔・努尔-艾尔丁和司机赛义德・查马赫遭美军袭击遇害的日子。当年，有关这起袭击的文字报道铺天盖地，但从未有视频公布。<br />
<br />
视频由阿帕奇直升机机载，机位靠近瞄准器。画面显示，一些人在巴格达一个社区广场上移动，而后直升机向他们开火，不少人倒地。<br />
<br />
一名飞行员嘴里不干不净地骂着地面上的人，还为他们的倒地叫好。<br />
<br />
网站发言人朱利安・阿萨热说，路透社摄影记者和司机就在那些倒地者之中。<br />
<br />
美国中央司令部发言人肖恩・特纳说，事后调查发现，直升机上美军士兵当时并不知道地面有媒体记者，误把记者当作武装人员，误把记者的相机镜头看成榴弹发射器。<br />
<br />
“维基解密”经常披露一些据称是揭示腐败行为的政府及企业“内部”文件。尽管标榜反腐，但其披露信息方式存在不少争议。<br />
<br />
向救援面包车开火<br />
<br />
在这段视频中，对地火力攻击导致人员伤亡后，一辆面包车驶入画面，试图营救伤者。飞行员说道:“快点，让我们开火吧。”他认为，这辆车上有武装人员。<br />
<br />
获准后，直升机向面包车开火，致车内外多人死亡。<br />
<br />
画面显示，美军地面部队抵达现场后，从车中抬出两名受伤儿童。一名飞行员的画外音说道:“把孩子带到战场，这是他们的错。”<br />
<br />
一名不愿公开姓名的美国国防部官员向路透社证实视频画面及画外音真实可信，但军方公开表态称，正在着手确认视频来源及其真实性、拍摄时间和地点。<br />
<br />
路透社总编辑戴维・施莱辛格说，视频真实地反映出从事战地新闻报道面临的危险。<br />
<br />
两年前的这起事件发生后，驻伊美军向美联社提供了一些有关路透社记者遇难事件的调查材料，当时的调查结论是，美军行为恰当，路透社记者可能“与武装人员混在一起”，其随身携带的设备让美军难以辨认他们的身份。<br />
<br />
射杀行为好似游戏<br />
<br />
网站发言人阿萨热不同意美军调查结论。<br />
<br />
他说:“我相信，如果那些杀戮合乎交战规则，那么交战规则本身就大错特错。”<br />
<br />
他批评阿帕奇飞行员的射杀行为“好似在玩一个电脑游戏，目标就是（通过杀死对手）获得高分”。<br />
<br />
路透社曾以目击者和伊拉克警方为消息来源报道，美军直升机当时向一辆面包车“连续地随意开火”，共打死11人，其中包括努尔-艾尔丁和查马赫。他们年龄分别为22岁和40岁。<br />
<br />
驻伊美军随后发表声明说，当时伊拉克安全部队正在和武装人员激战，美军直升机奉命前去支援，打死9名武装人员和两名平民。事后得知两名平民供职于路透社。</div>

							
							
							
							
