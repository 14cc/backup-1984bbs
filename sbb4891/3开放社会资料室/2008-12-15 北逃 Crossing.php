　　2007年，北韩咸镜道某村一家三口――父亲龙修、母亲容花，还有年仅十一岁的儿子俊伊生活清贫，却倍感幸福。某天，母亲容花突然累倒，得了肺结核。一家人连最便宜的感冒药都买不起，父亲龙修决定冒险远赴中国。经过一番磨难，龙修终于在一个伐木场找到了工作。由于该木场进行非法伐木行为，后被发觉，龙修不仅丢失了钱财，还不得不接受被警察追击的命运。一天，修龙听说只要参加一个“访问”就可以得到报酬，于是欣然前往。没想到，修龙就此和家人天各一方……<br />
<br />
　　修龙走后，容花的病情逐渐加重，终于不治，离开人间。无依无靠的俊伊决定不顾一切地找到父亲。已经身在韩国的龙修也通过经纪人寻找儿子的下落，盼望父子重逢。然而，这对父子两人却一次次擦身而过……<br />
<br />
一句话评论<br />
<br />
剧组花费了4年多的时间研究关于“逃北”的纪录片资料和对真实的逃北者进行采访，加上在中国和蒙古的实地拍摄，令影片给人诚意十足的真实感。<br />
――film2.0<br />
<br />
影片以结束一天煤矿工作的父亲与儿子一起踢球的场面伴随着美丽感性的旋律开场的，如此幸福的情景在后边如地狱般深重的苦难时总会在主角的记忆里闪过，相信即使是偏理性的观众也难以止住煽情的泪水。<br />
――film2.0<br />
<br />
时代悲剧的终结仅仅有“泪水”是不够的。导演的视线平淡无奇，没有进一步去发掘，是遗憾之处。<br />
――film2.0<br />
<br />
没有抬高任何“艺术姿态”，类似纪录片的平实。之前对逃跑者的偏见都解开了。身边的观众，看片过程中，叹息和眼泪不断。全片没有任何商业电影的趣味要素。<br />
――daum<br />
<br />
“担心观众受到的冲击过度，所以删去了一些更有‘火’的情节”导演的这句话与其说是解释创作意图，不如说是为自身掌控如此“宏大的主题”电影能力不足的解围之词。影片欠火侯。省略了很多必要的现实交代，主题显得模糊。<br />
――daum<br />
<br />
这是一部无法轻松喝着可乐咬着爆米花观看的电影。车仁表的演技有着突破性的上升。<br />
――daum<br />
<br />
幕后制作<br />
<br />
　　车仁表回归大银幕变身“温暖慈父”<br />
<br />
　　电影《北逃》是演员车仁表蛰伏两年后，首次回归大银幕。此次，他褪去都市男子的时尚气质，尝试挑战一位来自北韩咸镜道的 “叛逃者”。他不仅刻苦学习方言，还向真正的北韩“叛逃者”取经，聆听他们的切实经历，凄惨故事，培养自己的情绪。此外，为了在外形上更接近角色，车仁表放弃了热爱的健身运动，肌肉开始慢慢萎缩，体重下降了4公斤左右。已经身为人父的车仁表对“龙修”这一角色抱有共鸣，并用自己扎实的表演功力为影片护航前行。<br />
<br />
　　600：1脱颖而出的申明哲<br />
<br />
　　对《北逃》剧组来说，最难的事情并非“北韩”这个主题，而是否能够找到可以胜任 “俊伊”这一角色的小演员。根据剧本，这一角色需要较高的领悟能力，并非任何一个所谓“童星”可以胜任。剧组用了五个月的时间进行面试，在来自电影、电视剧、广告领域的600名候选者中一对一筛选，始终没有令人满意的对象出现。一天，导演金泰俊突然想到面试过程中一个名叫“申明哲”的孩子，并出现了一种乐观的直觉，决定再给其一次面试的机会。这是一次令人热泪盈眶的面试。申明哲的表演感染了在座的每一个人，导演金泰均的眼眶始终是湿漉漉的。于是，申明哲当之无愧地成为饰演“俊伊”的不二人选。<br />
<br />
　　取源于真实题材<br />
<br />
　　近来迅速萎缩地韩国本土电影市场仍有不可多得的人气佳作。《华丽的休假》、《我人生中最辉煌的时刻》、《追击者》等影片广受追捧，除了它们皆取材于现实生活中发生的真实事件，在还原真实的同时亦使观众产生强烈的共鸣，从而成为话题之作。影片《北逃》同样以丰富的真实素材为积淀，2002年3月，25名北韩人冲入西班牙驻北京大使馆等反应当代北韩人生活状态、思想状态的事件。影片制作方寻找了大量资料，并参考了反映北韩状况的纪录片及各种针对北韩人民的访谈等，力求影片情节的写实性，耗时四年完成。<br />
<br />
　　告诉你一个真实的“北韩”<br />
<br />
　　韩国之前曾经拍摄过大量反映北韩的影片，如标志着韩国本土电影崛起的《生死谍变》、《JSA共同警备区》、《台风》、《国境的南边》等。尽管题材十分广泛，但《北逃》着重刻画的是现今普通北韩人民的真实写照。影片公映后，观众在很长一段时间不能相信他们所看到的画面。他们未曾想到和他们同时代的北韩人民竟然有着这样令人心痛的故事。<br />
