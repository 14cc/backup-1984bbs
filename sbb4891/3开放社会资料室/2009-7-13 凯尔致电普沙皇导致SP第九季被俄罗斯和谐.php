俄罗斯２Ｘ２电视台近年来一直热播美国电视动画喜剧《南方公园》。不过英国媒体报道，电视台日前已将一季动画片中含有讽刺俄现任总理弗拉基米尔・普京的片段删节。<br />
<br />
&nbsp; &nbsp; 英国《每日电讯报》援引这家电视台发言人１０日的一段声明说，这季《南方公园》本月７日放映。电视台在放映前审查动画片内容时发现，其中部分情节有损普京形象，于是将这些段落删节。<br />
<br />
&nbsp; &nbsp; 《南方公园》１９９７年８月１３日在美国首播，收视率居高不下，在美国社会引起了很大反响，曾多次获得美国艾美奖提名。但这部动画片也因拿名人“开涮”、语言过于粗俗而颇受争议。<br />
<br />
&nbsp; &nbsp; 《每日电讯报》报道，尚不清楚删减有关普京段落的决定是由电视台领导层还是俄罗斯广播电视管理机构做出。<br />
<br />
&nbsp; &nbsp; ２Ｘ２电视台发言人安德烈・安德烈耶夫在一封电子邮件中向美联社记者承认普京段落被删节的事实，但没有交代原因。俄罗斯广播电视管理机构发言人叶夫根尼・斯特列琴科则以“不知情”作为回应。<br />
<br />
&nbsp; &nbsp; ２Ｘ２电视台去年曾因播放《南方公园》而惹上官司。俄罗斯福音信仰基督教联盟当时起诉２Ｘ２电视台，认为该台播放的《南方公园》中包含伤害信教者感情的内容。（新华社专电） <br />
