&nbsp; &nbsp;&nbsp;&nbsp;回国之决心已定，尚盼各方鉴谅&nbsp;&nbsp;此颂<br />
<br />
 近棋&nbsp; &nbsp;范曾于巴黎<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;一九九二年十一月二日<br />
<br />
 <br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;好自大与虚荣的文字，处处暴露着一个背叛者的矛盾与胆怯。行文一开头就以 “冷静回顾”交代了自己的错误，却遮遮掩掩，把叛逃牵强附会成“蛰居”；把背叛与出卖强化成“爱国”与“奉献”。用 “向来支持改革开放”来扮呼风唤雨的政治家的权威，用“海外的留学生”和国内的“莘莘学子”似乎在强调其无处不在的世界性“民意”。无疑是狡辩其叛国的“合理性”，有逼谏的手法。<br />
<br />
&nbsp; &nbsp; 那么，范曾为什么要 “冷静回顾”？为什么给国人与南开学子造成“遗憾”，并且极力要强调自己“向来支持改革开放政策”呢？《展望》杂志（八十年十月号）上，范曾的多年好友杨克非道出了范曾叛国的实情是：“对天安门事件的‘关怀’”。这就是连范曾自己都难以启齿，需要遮遮掩掩来检讨的真相。<br />
<br />
&nbsp; &nbsp; 翻开范曾曾在亡命巴黎落魄时给自己伪造的“平反”著作《辞国记》“客从东方来”篇，他说：“其实我的去留，无关人类命运的宏旨，不似某些大人物的病情足使股票升降”。这里，范曾的“倒邓保赵”阴魂不散，用“某些大人物的病情”“足使股票升降”映射批评邓小平同志与改革开放政策，否定“中国共产党十三届三中全会”的会议精神：“治理经济环境、整顿经济秩序、全面深化改革的方针”。甚至认为，“‘治理整顿’会导致‘停滞’”，肆无忌惮为推崇资产阶级自由化制造舆论。似乎只有叛逃才是“关乎人类命运的宏旨”的“正确”选择。<br />
<br />
&nbsp; &nbsp; 到了1993年，中国政府对于叛逃在外的暴乱分子给予了宽大政策。只要不发表新的言论，不加入新的组织，就可以回国，既往不咎。范曾于11月归国了。但国家对他有几项禁令，今后媒体不能宣传，不能报道他，他的活动官方人士不能参加。他的朋友说，范曾受制了多年，久经沙场，利用他的座右铭“外靠奸商，内靠官僚”逃脱了束缚，又一次上了央视，做了“良民”。<br />
<br />
&nbsp; &nbsp; 今天，真相已经大白，范曾还口口声声炫耀自己的“忧乐国天下”情怀，让之憎恶。所谓的“忧乐国天下”，其实就是一场反革命阴谋与他逃亡海外后在政治上的落魄与绝望。他“忧”的是亡命天涯的日子，政治破产、生活困窘；“乐”的是打打杀杀与亡命天涯后的一个梦，“推翻中国共产党，颠覆社会主义政权”建立所谓的新“国”和“天下”。恐怖至极！<br />
<br />
范曾现象值得深思<br />
<br />
&nbsp; &nbsp; 纵观范曾人生的方方面面，投机与背叛是关键词。这类人的命运与性格，无疑与多变的社会大环境有关。<br />
<br />
由于我国曾经长期处于复杂的政治运动之中，使一些人尝到了投机局势，弄虚作假的甜头，见利忘义，做了变色龙。而范曾就是这类人的代表。每每在动荡中毫无原则、翻脸不认人，不择手段在混乱中“发家致富”，留下了千古骂名。成了今天车间生产的工人、田间地头识点字的农民、退休养老的社区老人、喜好艺术的文人和政客消遣、谈论、指责的工具。其劣迹永远将成为人们津津乐道消遣与趣谈的灰色经典，和正襟危坐、传道授业的反面教材.<br />
