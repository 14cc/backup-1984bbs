“爱国”：李瑞环批范曾“毫无人格，毫无国格”<br />
<br />
&nbsp; &nbsp; 叛国，这是范曾一生的重要真相，也是当代艺术界教化下一代的反面经典。<br />
<br />
1989年，以严家其为首的暴乱分子，与国外政治势力内外勾结，煽动和制造了一场推翻中国共产党领导，颠覆社会主义的中华人民共和国的反革命运动。他们漫骂共产党是“一代奸党”，高喊着“打倒共产党”的口号。并且组成了“敢死队”、“飞虎队”、“义勇军”等恐怖组织，扬言要绑架党和国家领导领导人，要用“攻打巴士底狱”的方式夺取权力，要“秋后算账、非算不可”，要算党的帐，算政府的账，并且准备了要镇压的干部名单。台湾艺术界等境外势力踊跃捐款，支持学生打、砸、烧、抢。许诺烧一辆军车给3000元，抓住或打死一个军人也给几千元。据《中国时报》（中华民国八十年十二月二十七日星期日）透露：“北京民运高潮之际范曾致书吾尔开希并慨捐五万元人民币；事件爆发，军队镇压民运之后，倾向撇清与民运关系，甚而带有指责”。如此令人毛骨悚然的行径，却也蕴涵着两面三刀。一面是唯恐天下不乱与火上浇油，另一面是谦谦君子与一本正经。好不讽刺！他6月3日还对崔某扬言要“明天（6月4日）就到天安门最前线绝食静坐去”，6月4日以后就不敢承认了。<br />
<br />
&nbsp; &nbsp; 有人说的对，范曾就是靠投机活着，见风使舵、见利忘义，没有什么原则可言。<br />
<br />
&nbsp; &nbsp; 反革命暴乱平息后，严家其和“高自联”头目吾尔开希在国外势力的策划下逃亡法国，于同年7月4日在海外发表了所谓《国殇周日宣言》，成立了“中国学运民运联合委员会”，要在中国大陆制造更多的“风暴”。这时的严家其为了拉拢势力，收买人心，就开始和范曾来往了起来。范曾以为，追随严家其定会干出一番大事，有做“革命元老”的希望，致使他做出了一生无法抹去的罪孽。<br />
<br />
&nbsp; &nbsp; 1990年11月初，范曾在北京公安局十三处属下艺苑画廊（现为中国艺苑）负责人于金英女士（已故）和当时已退休的公安局某副局长的陪同下，抵新加坡举办完画展，回国途经香港，住到君悦酒店。此时，严家其已从巴黎悄然飞往香港与范曾会合，接应其叛逃，给范曾办理好了签证手续，并亲自监督范曾来执行叛逃前的任务。范曾趁陪同人员吃饭之际，匆忙跑到君悦酒店1212房间和情人楠丽做了“生死告别”，直奔机场。面对提前安排好的媒体，情绪高涨地发表了《辞国声明》，说“自己是有良知的知识分子，对共产党镇压、迫害民主人士（反革命暴乱分子）不能容忍”，热情激昂地赞颂了一番反革命暴乱行径。于11月5日与严家其乘法航仓惶逃往法国巴黎，《东方日报》等港台各报纸头版头条报道了此事。<br />
<br />
&nbsp; &nbsp; 后来，万里儿子万忠祥对好友吴某说，“范曾去新加坡办展，请我父亲万里发个贺电，贺电发了他就叛逃了。害得我父亲因为犯了没有政治预见性错误，给中央政治局写了检查”。当时李瑞环也给范曾发了贺电，没想到中了范曾的阴谋。所以就怒骂“范曾毫无人格，毫无国格”！<br />
<br />
&nbsp; &nbsp; 《人民日报》1991年3月20报道，政协十届闭幕会议根据《中国人民政治协商会议章程》第二十八条的规定，决定撤销范曾中国人民政治协商会议全国委员会委员资格；21日，民盟举行第12次中常会，鉴于范曾严重违反《中国民主同盟章程》，会议根据《中国民主同盟章程》第36条规定，决定撤销了范曾中国民主同盟中央委员会委员及民盟中央常务委员会任命的职务。这就是相关部门对范曾叛国一事的官方处理结果。<br />
