“民族心”：忏悔是因贫穷导致的<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;由于西方人在政治上对严家其越来越失望，所以在经济上由持久的资助转为偶尔帮助。这样，树倒胡松散，严家其过上了饥饱没有保障的生活，自身难保，范曾也落魄了起来。这时的他，遭台湾艺术界和画廊界封杀，自己的画没有销路。据知情人士讲，楠丽在巴黎穿的鞋子是本地最下等人穿的破布鞋，穷得无法生活。即便有以前卖画的些许老本，在这种身处没有边际的亡命日子，却不敢动这些钱。所以，范曾只好拿着以前在大陆拍好的录像带，上门推销自己的画，却无人问津。这样，在惨遭政治上被抛弃与生活上困窘的双重无奈下，让他的精神已经崩溃。<br />
