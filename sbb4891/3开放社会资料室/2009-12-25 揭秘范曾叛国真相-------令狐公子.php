令狐公子 @ 2009-8-4 10:10&nbsp;&nbsp;<br />
<br />
过去的一年，是多事的一年。范曾最终在灾难中得到了救赎与投机的机会，不仅赢得了体制，也赢得了同情，身负20年之久的“枷锁”终于被解除。上电视登报，这个因叛国而本不可能的夙愿，在忏悔路上最终变为现实。这一次，体制是娘，传统是爹，他得又一次“背叛”自己来为他批斗和叛变过的一切歌功颂德。所以，我们不必惊慌！范曾在复出后的第一次亮相，在央视上谈“艺术”、谈“文化”、谈“孝道”、谈“爱情”、谈“情意”、谈“民族”、谈“爱国”，其实只是逃脱禁锢后的一次重大思想改造工作汇报。即便是他虚情假意，也只能装作虔诚。<br />
<br />
&nbsp; &nbsp; 对范曾而言，当下最重要的就是切实贯彻“外靠奸商，内靠官僚”的座右铭。在金融危机这个大环境中，拯救自己一落千丈的绘画市场，重新塑造自己破败不堪的“大师”形象，和正在“大师计划”路上高喊“艺术属于人民”的吴冠中先生，以及国画界一决高下。所以，他就和商家联合斥资1000万制造了“范曾十二生肖大全套金币及五子牛黄金画卷”来张扬自己的实力。在媒体新闻稿中，利用违禁的广告词，如“宗师”、“国画第一人”、“无可争议的权威性和真实性”、“其价值无人能与之争锋”、“要集齐全套12种生肖金币，不知道要等多少年”、“以最高规格纪念祖国60华诞”、“ 绝对是越传越值钱的传家宝”等片言，似传销令人激昂，却似丰胸、壮阳广告让人的每一个器官都充血，令人麻痹。如此宣传方式，除了自大和自封，抢客户以外，有着浓烈的老王卖瓜、气急败坏和人斗富、斗气的意思。<br />
<br />
&nbsp; &nbsp;<br />
<br />
&nbsp; &nbsp; 不料，，中央电视台《经济半小时》栏目在“3・15”晚会后续报道中揭露了范曾假金币和假金画卷，均未获得 《经营黄金制品核准登记证》，也没有得到中国人民银行发行黄金纪念币的许可。并且广告中所称35克黄金的五子牛黄金画卷所含黄金不足1克。涉嫌制假贩假，诈骗近亿元。范曾及其商家被一消费者告上法庭。这让一些收藏过他画的画贩子和一些靠倒卖艺术品养家糊口的收藏家们赔了老本又骂娘！<br />
<br />
&nbsp; &nbsp; 所以，无论是过去，还是今天，范曾的存在依旧是一把双刃剑，损人利己，人人喊打！他除了多变的佞言，每前进一步，总是惊世骇俗，让人诅咒又讥讽。<br />
<br />
&nbsp; &nbsp; 我今天所写的主要内容，对于范曾的真相只是九牛一毛。如果范先生有异议，期待回应！<br />
<br />
艺术：《文姬归汉》是范曾投机的产物<br />
<br />
&nbsp; &nbsp;人们对范曾艺术的质疑，无疑是继其品格之后的一大焦点。艺术史的真理就是艺品与人品相兼，人品不高，艺术无趣。所以，谈范曾必须谈他的品格，方可给予其艺术上的定位。<br />
<br />
&nbsp; &nbsp; 范曾的座右铭是“外靠奸商、内靠官僚”，这是众所周知的。台湾名人画廊负责人胡云鹏曾说，“范曾的艺术是托人家的命活着的”。现在看来这话很有概括性，如1962年范曾在央美的毕业创作《文姬归汉》就是一个典型。<br />
<br />
&nbsp; &nbsp; 近期，范曾在央视《艺术人生》栏目中又将《文姬归汉》搬了出来，云：“郭沫若看到这画后，高兴得‘夜不能?rsquo;，就题了很多（辞），而且说‘以后画多少给题多少’”。这是真相吗？那么，郭老为何给范曾题了画？又为何在以后一幅都没有再给题呢？<br />
<br />
&nbsp; &nbsp; 道理很简单！<br />
<br />
&nbsp; &nbsp; 第一、1962年郭沫若先生的《蔡文姬》话剧全国热演，范曾抓住了郭老的“蔡文姬”情结，识时机地画了同样题材的画儿迎合郭老。据知情者讲，当时范曾夹着画在郭老家门口守候数日，一次，郭老的车到了门口以后，范曾就箭步上前，由于郭老不认识他就没搭理。其秘书王廷芳下车了解了情况后就把画捎了去。郭老打开画一看“蔡文姬”，和他的话剧题材一样，而且是小青年画的就给题了。假如当时范曾画的是“老子”、或者“怀素”，郭老会给题吗？这就是后来郭老一幅都没有给题的答案。所以，《文姬归汉》纯属投机。与其说郭老给范曾的《文姬归汉》题辞，还不如说郭老给自己的“蔡文姬”热演后的社会反馈资料写评语；或者说，郭沫若先生的“夜不能眠”，不是因为看到了范曾的《文姬归汉》，而是他亲眼目睹《蔡文姬》热演后的成功让各行各界认可，乃至画界连小青年都追随效仿他；<br />
<br />
&nbsp; &nbsp; 第二、郭沫若先生给范曾题画只是为了提携青年一代，换做是别人画《文姬归汉》，郭老照样给题。这就好比读者来信一样，总有一个幸运者被回复。<br />
<br />
&nbsp; &nbsp; 第三、公众人物给追随者签名是正常的声誉与形象保持手段，这就是郭沫若先生给范曾题画的最通俗的答案。与当下的歌星、球星、影星给“粉丝”赠签名唱片、签名战靴、签名光盘等有如出一辙的道理。只不过范曾借势造势，将此事作为宣扬的工具罢了。<br />
<br />
&nbsp; &nbsp; 郭沫若给范曾题了画后，就再没搭理过他。范曾为了接近郭沫若，就开始讨好郭老的长子郭汉英之妻谢贝岭。每次碰面都称谢为“恩姐”，弄得人家很反感他。在当时，谢贝岭负责郭家的所有事情，要接近郭老，必须要过谢贝岭的关。加上谢的父亲是一位将军，而且她本人也是电影局艺术处处长。所以，范曾才不厌其烦，将原本不认识的人称“恩姐”，弄得别人一头雾水，时间长了就开始烦他、躲他。如此情形，谢贝岭实在无奈，就通过吴某给范曾传话：“悦石，你认识有个叫范曾的人吗？这人我根本不认识他，总是叫我恩姐，我很讨厌他。郭老给他题画，只是出于社会责任提携了下青年人而已，他没有必要这样感激我”。<br />
<br />
&nbsp; &nbsp; 事实上，范曾巴结的不光是谢贝岭，只要是郭家的人他都巴结。郭沫若的子女郭建英、郭平英都称“并不认识范曾，很讨厌看见他”。<br />
<br />
&nbsp; &nbsp; 原来，范曾为了接近郭沫若先生，如此用心良苦，低三下四惹人讨厌！<br />
<br />
文化：“批林批孔”画是范曾反传统的证据<br />
<br />
&nbsp; &nbsp; 现在，范曾成了传统的“卫道士”。在前段时间的央视《我们》栏目上，他滔滔不绝背《离骚》、侃孔孟之道，让世人又一次迷惑了起来。当初这位“批林批孔”的先锋，在“文革”中表现得尤为积极，画一些打打杀杀的造反画儿来铲除传统文化，今天却一脸正经地讴歌“和谐”、诵读“孔孟”。尤为讽刺。<br />
<br />
有人说的对，“范曾翻脸比翻书还快”！<br />
<br />
&nbsp; &nbsp; 他以为，时过境迁了，画造反画儿的事情人们已经忘掉了，所以就毫无忌惮地转换成正面形象，来投机和谐盛世。事实上，他曾经给荣宝斋，以及一些杂志报刊作了许多“批林批孔”的画和插图，这永远都成为人们揭发他的罪证。今天，他正在用佞言迷惑青年一代，文艺界要做的，就是拿出证据，拆穿这个阴谋，来坚守正道。让其在指责声中继续检讨与忏悔。<br />
<br />
&nbsp; &nbsp; 1974年， 江青、王洪文等“四人帮”成员为了篡夺权力，大肆宣扬“女皇”舆论，为反周“组阁”造声势，发动了一场“批林批孔”运动。每逢运动，范曾尤为积极。他在这个时期，画了许多关于黄巾军、李自成、义和团等农民起义造反的插图和画来批判孔子和儒家思想，如《黄巾军的反孔斗争》（《革命接班人》杂志1974年第12期）、《中国农民反孔斗争》（1976年作，尺寸39×27cm×14，2008年3月23日中国嘉德四季第13期拍卖会）等。他认为，只要消灭了儒家思想，才能“兴洛仓”、“法平等”、“均贫富”、“等贵贱”，其中《中国农民反孔斗争》这幅插图，就是典型的范曾反孔的证据。<br />
<br />
&nbsp; &nbsp; 过去，孔子和儒家思想在范曾眼里就是十恶不赦的“破坏分子”，他甚至把社会的贫富差距等因素也归罪于儒家思想。今天，范曾一反常态，又高歌孔孟之道，说明他很投机局势，只要是对自己有利的，他都会毫不犹豫去做。<br />
<br />
&nbsp; &nbsp; 连文化都背叛的人，还能称得上文人吗？所以，我们在认清范曾的同时，就等于认清了当代艺术史的所有反面真相。在文化生态中，范曾的存在，无疑是乡愿们的代表，投机与捣乱，消极意义无穷。<br />
<br />
爱情：曾抛妻与楠丽通奸了20年之久 <br />
<br />
&nbsp; &nbsp; 范曾的爱情真相，是他情感和生活的全部底细。前妻边宝华是从好友马某手里夺来的。因此，勇夺朋友之妻一事，让艺术界鄙视他至今，使他成为了坏人品的代言人。<br />
<br />
&nbsp; &nbsp; 在别人眼里，范曾的感情是龌龊的。而范曾自己反倒觉得自己是“至情至性”的。真相到底是怎样呢？<br />
<br />
范曾从好友马某手中夺来了边宝华之后，结婚五年左右，于1971年夏在一次聚会中认识了楠丽。当时楠丽身着一件雪白的连衣裙，两条辫子乌黑油亮。范曾用了“春梅绽雪、秋慧披霜”八个字来形容她的素洁。<br />
<br />
&nbsp; &nbsp; 1972年，范曾背着妻子边宝华和楠丽通奸的事情被楠丽的老公发现，范怕相关部门找他谈话，或者要面临坐牢，就仓惶逃到了秦皇岛。其友崔某怕事情闹大，召集了一些朋友找人商量将事情摆平：第一向人家保证不再犯；第二赔礼道歉；第三就是拿钱赔偿。等万事大吉以后，范曾回来后大骂崔某说泄露了“机密”，并且提出绝交。 对于此事，我拜访崔某时，他的立场格外坚定：“范曾和楠丽20余年的爱情，的确是背着边宝华通奸”。<br />
<br />
&nbsp; &nbsp; 事实就是这样，范曾在1992年11月2日，给丁关根的检讨信中写道：“今我已于原偶分居逾三年，离婚之事当不成问题，这对我亦如释重负”。这样看来，他和楠丽的20余年爱情属于通奸了。<br />
<br />
&nbsp; &nbsp; 好笑，在央视《艺术人生》栏目上，范曾说他84年至85年就和楠丽商量捐建南开大学东方艺术大楼的事情，当着全国人民这是一个不小的谎言。那个年代，通奸是要坐牢的，谁还敢和情人公开商量这么大的事情？范曾对此会做何解释呢？<br />
<br />
情义：沈从文批范曾“损人利己”<br />
<br />
&nbsp; &nbsp; 八十年代末，台湾艺术界与画廊界有这样的一句话：“别人往上爬楼梯，范曾往上登别人的性命”。这话揭露了范曾无情无义，过河拆桥的本质。沈从文曾经就是范曾利用后的牺牲品。<br />
<br />
&nbsp; &nbsp; 范曾在央美即将毕业，为了得到一份做插图的工作，天天给沈从文写信，对沈格外关心。一次，天刚亮，范曾就敲开了沈家门，说，“昨晚梦见先生生病，我不放心，连夜从天津赶来”，这话让沈从文很感动。于1962年，在沈从文先生的帮助下范曾调到了历史博物馆美术组，给中国古代服饰做插图。陈大章是他的组长。<br />
<br />
&nbsp; &nbsp; 在历博工作没多久，文革到来，沈从文成了当时被批斗和揭发的对象。范曾摇身一变成了“中央派来的工作组”，指责沈先生“牛鬼蛇神、自身有严重的错误，有病需要治”。所以就专门负责监督和“帮助”沈从文“思想改造”。并特辟专栏，给沈先生写了几十张大字报，列举了几百条严重错误，说“沈从文头上长脓包，烂透了写黄色小说，开黄色舞会”。这些大字报让沈从文先生足足“学习”了三半天。<br />
<br />
&nbsp; &nbsp; 沈从文在１９６６年７月《一张大字报稿》中用了“十分痛苦、巨大震动”八个字来概括这事。说“揭发我最多的是范曾，到我家前后不会少十次，有几回还是和他爱人同来的。过去老话说，十大罪状已够致人于死地，范曾一下子竟写出几百条。我只举一个例就够了，即范曾揭发我对群众最有煽动性的一事，说是丁玲、萧干、黄苗子等，是我家中经常座上客，来即奏爵士音乐，俨然是一个小型裴多菲俱乐部。这未免太抬举了我。事实上丁玲已去东北八九年，且从来不到过我家中。客人也十分稀少，除了三两家亲戚，根本就少和人往来。 来的次数最多大致便是范曾夫妇，向我借书主要也只有你夫妇。你怎幺知道丁玲常来我家中？这究竟是怎幺回事？别的我就不提了。即使如此，我还是对范曾同志十分感谢，因为他教育了我，懂事一点，什幺是‘损人利己’。可说是收获之一 ”。<br />
<br />
&nbsp; &nbsp; 这是铁证如山的真相！<br />
<br />
&nbsp; &nbsp; 范曾以前的朋友这样评价范曾：“范曾通过朋友介绍，只要认识了更高地位的人，就踩踏朋友，翻脸绝交；通过局长认识了部长，就踩踏局长，绝交。所以，千万别给范曾介绍比你厉害的人”。<br />
<br />
&nbsp; &nbsp; 1983年，范曾戏弄新华社外事局局长杨荣刚一事，让人哭笑不得。范曾要去趟法国和加拿大，由于那边不认识人，所以需要熟人接待，就托吴某帮忙。吴某找了新华社原社长曾涛，曾涛安排杨荣刚帮忙办理这事。杨局长答应了，和曾涛、吴某三人应邀去了范曾家。范曾很激动，也很恭敬，一副求人相，当场就说“曾社长我给你画一幅好画”，接着又给杨局长也答应了一幅。没多久范曾去了温哥华新华分社，吃、住、行新华社杨局长接待得很好。等一回国，范曾见到杨局长说，“你是曾社长的司机吧”？“这才几天，一个局长变成司机了”！吴某当场就懵了。杨荣刚很大度，说“是，我是曾涛的司机”。事后，他对吴某说，“这种人（范曾），永远别拉我去见他”。<br />
<br />
 <br />
<br />
孝道：恩师对范曾的评价是“中山狼”<br />
<br />
&nbsp; &nbsp; 范曾最近当着媒体谈李苦禅先生，一幅“孝悌之道”的正经，反倒勾起了人们对他的怀疑。事实上，范曾曾是苦老的学生，由于范曾对苦老不敬，师徒之间的关系最终决裂。<br />
<br />
&nbsp; &nbsp; 八十年代初的一天，单昭祥先生在吴某的陪同下去拜访苦老。刚到苦老家，还没坐稳，苦老很不高兴。单老急忙问道：“怎么了”？苦老迟疑了半天，嘴唇煽动了几下，极为委屈的样子说：“你认不认识有个叫范曾的画家”？单老一听事情不好，范曾这家伙肯定又踩踏了老师。然后说，“据说是你的学生”？苦老开门见山就骂道：“子系中山狼，得志便猖狂”。给单老足足骂了几个小时。<br />
<br />
&nbsp; &nbsp; 原来范曾在画卖到一尺七元时，画价和苦老、董寿平等老先生的一样高，就得意忘形了。见了苦老，不再喊老师，而直接拍着苦老的肩膀喊“苦禅”。并且总是摆出一幅盛气凌人相，对苦老指手画脚，带有指责。长此以往，苦老也最终选择了在离世前的最后时刻，立下口头遗嘱：“没有范曾这个学生”。特意交代“死了别让范曾扶灵”。古语道：“人之将死，其言也善”，可以想象范曾对恩师的伤害是何等重大。<br />
<br />
&nbsp; &nbsp; 苦老去世的那一天，葬礼是按照国家重要首长的规格办的，场面极为壮观。政界高层、艺术界权威，以及各界有头有脸的大人物都来了，现场甚至出动了武警警卫。一看这场面，范曾也来了，拿出了其搞“运动”的表演天赋，在门外哭得“惊天动地”。但苦老的学生以及子女，为了响应苦老的遗嘱，拒绝他给苦老扶灵。万兆元（已故）先生一看范曾一个人在门外哭闹，死不罢休的样子会影响葬礼秩序，就帮忙说情让范曾参加了苦老的葬礼。<br />
<br />
&nbsp; &nbsp; 我今天把这个事情拿出来，不是专门揭范曾的短处，而是让他实事求是，坚守原则。知道做过的事情就是泼出的水，无法收回，也无法回避，只能收敛。<br />
<br />
我们在听范曾讲故事时，必须要了解他的真相，以免被迷惑！<br />
