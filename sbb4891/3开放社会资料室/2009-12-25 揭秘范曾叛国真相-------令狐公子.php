丁关根同志：<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;我于一九九零年秋辞国蛰居巴黎两载，冷静回顾之后，决定回国，原因有四：<br />
<br />
&nbsp; &nbsp; 一、 在国内我一向爱国主义和风险意识，而辞国远走与自己内心抱负相悖。国外各国留学生住处几乎都挂有我为教委所题赠他们的字幅“月是故乡明”、“砥砺品学”、“尤乐国天下”等耿耿情怀至今依然；<br />
<br />
&nbsp; &nbsp; 二、 自一九八五年为天津南开大学“二年画一楼，两鬓添秋霜”，今□楼已成，我所手建立的东方艺术系亦已开学两年，莘莘学子，我所深爱至今未见我一面，我的辞国带给全系师生无可言说的遗憾和痛苦，此亦我远居异域，内心不安的主要原因。<br />
<br />
&nbsp; &nbsp; 三、我一向支持改革开放的政策，年初以还，国内请方正及需用人之际，我愿接近□力，继续为中华民族振兴风斗二十年。<br />
<br />
&nbsp; &nbsp; 四、 我辞国主要原因其一是家庭生活之不睦，今我已于原偶分居逾三年，离婚之事当不成问题，这对我亦如释重负。<br />
