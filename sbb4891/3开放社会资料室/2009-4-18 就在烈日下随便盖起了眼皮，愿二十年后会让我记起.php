就在烈日下随便盖起了眼皮，<br />
愿二十年后会让我记起。<br />
――周耀辉（黄耀明《20》）<br />
<br />
<br />
广场行<br />
君不见我为人间惜芳菲，<br />
何为使我心事违？<br />
昨夜千山啼杜宇，<br />
声声唤春春不归。<br />
尝闻我生癸亥岁，<br />
虬枝轻红初破蕾。<br />
乱花渐渐迷人眼，<br />
东风忙与论功罪。<br />
君不见明公身灭名长存，<br />
断煞京师学子魂。<br />
人间空遗多少恨，<br />
一朝事起天安门。<br />
愿罢特权惩官倒，<br />
新闻求真岂胡造。<br />
民主倘使能治国，<br />
四海妖氛势尽扫。<br />
双手捧上救民箴，<br />
请君恤我诤臣心。<br />
跪遍玉陛九十九，<br />
紫禁宫苑几许深。<br />
孰料君心真似铁，<br />
社论一出天下绝。<br />
亿万黎民塞街衢，<br />
欲图大冤一时雪。<br />
首者喉干复气噎，<br />
痛詈商纣与夏桀。<br />
从者呼声巨如雷，<br />
雷声震震沸人血。<br />
亦有机关同媒体，<br />
非徒学子与工人。<br />
更兼巧匠持鬼斧，<br />
铸得女神代赤神。<br />
当时名士苏戴辈，<br />
舌上莲花勤诱诲。<br />
壮夫纷纷逞豪勇，<br />
美名谓之飞虎队。<br />
四海之内怨渐升，<br />
理性对话苦未能。<br />
一自戈君访华后，<br />
世局已同炭与冰。<br />
始知异化事非小，<br />
恰似骅骝陷泥沼。<br />
大树既倒楼亦倾，　　　　<br />
马车唯余邓与赵。<br />
赵公赵公尤可哀，<br />
阙廷闾巷共疑猜。<br />
萧墙生变走高丽，<br />
未防同袍结鬼胎。<br />
五月十九动风雷，<br />
广场夜巡高士陪。<br />
鸟之将死声戚戚，<br />
终使老泪枯瀛台！<br />
书生书生休对泣，<br />
尔今奉身惟绝粒。<br />
欲填苦海先苦行，<br />
夷齐之事或可及。<br />
已而九州共嗟呀，<br />
复闻倡义遍天涯。<br />
香江自是多情水，<br />
民主歌声献中华。<br />
忽传京都迎虎骑，<br />
四十年间转相类。<br />
童子哭央妪担浆，<br />
七尺男儿岂无愧。<br />
相峙无计日渐移，<br />
公器何曾许逐私？<br />
倒悬江山余一线，<br />
酣沉梦远人不知。<br />
举头三尺何未睹，<br />
忍教弹雨换血雨！<br />
可怜惨雾愁云夜，<br />
长安市井添新脯。<br />
十里赤焰冲斗牛，<br />
市民性命等蜉蝣。<br />
不见女神犹齑粉，<br />
哪个凡夫敢淹留？<br />
忽地人海作兽散，<br />
偏有少年真铁汉。<br />
爷娘嚎哭拦不得，<br />
誓将微躯蹈国难。<br />
同学今日义同死，<br />
逢此世变可逃焉？<br />
海隅侯生称君子，<br />
谓宜饮恨换保全。<br />
雄鸡一哭天下白，<br />
君王覆手判凶逆。<br />
死者无言生者哑，<br />
此事人间浑无迹。<br />
荒唐国里多荒唐，<br />
断肠劫后更断肠。<br />
南望海峡应有泪，<br />
近羡柏林已无墙。<br />
满腔孤愤书一纸，<br />
欲求铁函藏心史。<br />
或问今夕是何年，<br />
四顾茫茫羞欲死。<br />
当日号呼报国士，<br />
大半从商小半仕。<br />
圣朝而今重行乐，<br />
行乐哪管朱与紫！<br />
车打左灯偏右行，<br />
盛世人人歌太平。<br />
愚不可及智可及，<br />
试问黄河清未清？<br />
君不见窃据要津多豺狗，<br />
贱民仍复无所有。<br />
更有河蟹钳人舌，<br />
言不成言但某某。<br />
文擘诗宗事犬儒，<br />
专家教授竞痴愚。<br />
东邻有子才高妙，<br />
忤时傲物艰世途。<br />
劝汝毋须长太息，<br />
何不大笑舒胸臆？<br />
人间正道终不废，<br />
三百年后定曲直。<br />
君不见我亦蹭蹬情何堪，<br />
幸耽读史事早谙。<br />
廿载兴废原蕉鹿，<br />
说与来人作笑谈。<br />
