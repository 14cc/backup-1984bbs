<br />
四川省公安厅信访办：<br />
&nbsp; &nbsp; 我叫艾未未，不服成都市公安局信访办在2009年11月16日对本人做出的答复意见（“处理信访事项答复意见书（编号（2009）5号））”，现申请四川省公安厅进行复查。<br />
&nbsp; &nbsp; 本人提出复查的事实依据如下：<br />
&nbsp; &nbsp;&nbsp; &nbsp;成都市中级人民法院定于2009年8月12日公开开庭审理谭作人涉嫌煽动颠覆国家政权罪一案，辩护律师浦志强、夏霖于8月10日向成都中院提交了通知证人出庭的申请书，我是被申请人之一。我应两位辩护律师的邀请，与我的助手等人于庭审前一日到达成都，12日零点左右入住成都安逸158酒店，准备在谭案中出庭作证。<br />
当天凌晨3点左右，成都市公安局金牛分局和西安路派出所几十名警员及其它身份不明人员，在西安路派出所邱勇所长的带领下，未出示警证和搜查证，强行要求进入513（艾未未）、515（左小诅咒）、508（刘艳萍、臧一）、510（赵赵、李肇豪）、512（晏丽芳、赵颖）、516（刘耀华，李心）、518（张劲松、徐烨）等7个房间进行搜查。我要求对方出示证件遭拒，拨打了110报警电话。在报警的时候，我的房门被踹开，十余名人员进来对我进行推搡拉扯，阻止我继续报警，其中一名警察对我的进行了殴打，重击了我右边脸颊，衣领、两袖均被撕破。随后警方对7个房间进行了搜查，将徐烨、张劲松、刘艳萍、臧一、刘耀华等人带到西安路派出所讯问，并命令我、左小诅咒等7人不得离开房间，不得对外通讯。我被殴打后，右边脸颊明显红肿，出现了耳鸣、头昏、咬合困难、头晕和恶心等症状，凌晨5点45左右，在左小诅咒陪同下，警方和我到成都中医院大学附属医院进行检查，CT诊断报告书表明“右侧面部软组织略显肿胀”，医生开了止痛片等药品。凌晨6点半左右警方将我带回宾馆继续监视。直到当天下午14点，警方才释放了我，非法拘禁时间达11个小时，期间没有给予任何解释和手续。<br />
8月13日下午，我到西安路派出所和金牛区公安分局反映问题，金牛区公安分局政委徐晖、法制科科长徐杰进行了接待，并做了书面记录。14日上午，我到成都市公安局信访办，投诉西安路派出所暴力执法、非法限制人身自由事件，成都市公安局督察大队何林警官、王警官受理了我的信访事项，并出具了《公安机关受理信访事项告知单》（编号【2009】03号）。<br />
当天我回北京后，出现以前未有过的持续头痛、失眠及记忆力下降，9月份我在德国慕尼黑布展期间，上述症状急剧恶化，9月14日我被送到慕尼黑大学临床医院神经外科急救诊室，经过核磁共振影像显示，我的“大脑表层组织之间有明显的淤血，硬脑膜右侧之间有清晰的挤压（硬膜下血肿），并且右脑半球异位”。该医院对我进行检查的医学博士特卡尔保勒医生报告认为“检查结果是危及生命的脑膜下血肿。考虑以往病史，这个血肿肯定源于脑外伤，也许是今年8月12日警察的袭击所致”。由于有生命危险，当晚10：30分由Tonn教授施以手术，在头颅右前侧打了两个血肿吸干孔，将脑硬膜下积液排出。9月21日我才被获准出院（诊断、手术证明见附件4、5、6）。<br />
&nbsp; &nbsp; 2009年10月13日，我接到成都市公安局信访办电话，通知我由于案情复杂，需要延期一个月答复。但一个月过去后，我仍未收到任何答复。2009年11月16日下午，我打电话到成都市公安局信访办询问答复情况，信访办值班工作人员拒绝告诉我名字和答复情况，并且挂断本人电话，我再次拨打后，他们竟然拔掉电话线，导致无法打通。直到11月19日，本人才收到成都市公安局信访办出具的“公安机关处理信访事项答复意见书”，答复内容为“经调查，金牛区分局西安路派出所民警在处警过程中，严格依法履职，不存在非法拘禁等问题”。（答复意见书见附件2）<br />
&nbsp;&nbsp;本人认为，成都市公安局信访办的答复中没有调查经过，没有提出证据，其答复是不符合事实的，也是无法接受的。我现对此答复意见提出以下复查请求：<br />
1、&nbsp; &nbsp; &nbsp; &nbsp; 查清警方当天是否对我进行殴打的问题。2009年8月12日凌晨，上述人员破门而入后，对我头部进行殴打，导致我出现了头痛、失眠、恶心和记忆力等下降，在一个月后我出现深度昏迷，并进行了脑部手术，警方的殴打行为对我的身体健康已经造成严重的伤害，已构成了涉嫌故意伤害罪。在此，我提供被殴打新的证据，即我9月14日在德国慕尼黑大学临床医院所做的脑部检查和手术证明（见附件4、5、6）。在8月12日凌晨的行动中，警方至少携带4台摄像机进行了全程录像，我要求公开、调看上述录像资料，以查清警方暴力执法问题。<br />
2、&nbsp; &nbsp; &nbsp; &nbsp; 调查我被非法拘禁的问题。警方从8月12日凌晨3点到当天下午2点，限制我人身自由达11个小时。我要求调查2009年8月12日凌晨，是哪些人员对我所住宿的安逸158宾馆抚琴店进行搜查？出于什么理由要对我的房间进行搜查？在12日凌晨对我房间进行搜查之前，是否已发现我有违法犯罪嫌疑？具体是哪些违法犯罪嫌疑？警方进入我的房间搜查后，是否发现我有任何违法犯罪嫌疑或可疑赃物？如果没有，警方对我的人身自由限制11个小时的依据是什么？警方这些行为是由哪级公安机关、哪个部门、哪位负责人进行审批的？<br />
3、&nbsp; &nbsp; &nbsp; &nbsp; 投诉成都市公安局信访办未在法定期限内办结信访事项。本人在8月14日到成都市公安局信访办进行了信访，对方受理我的信访，并出具了《公安机关受理信访事项告知单》（编号【2009】03号）。10月13日，以案情复杂为由，要求延期一个月，直到2009年12月16日，在本人电话多次催促下，才出具了信访事项答复意见书，从受理到办结共95天，已超过了最长的法定日期5天。《信访条例》第四十四条规定“推诿、敷衍、拖延信访事项办理或者未在法定期限内办理信访事项的，应该由上级部门责令改正或依法予以行政处分”，本人要求对成都市信访办推诿、敷衍，未在法定期限内给予办结依法进行处理。<br />
4、&nbsp; &nbsp; &nbsp; &nbsp; 《信访条例》第三十一条规定“对重大、复杂、疑难的信访事项，可以举行听证。听证应当公开举行，通过质询、辩论、评议、合议等方式，查明事实、分清责任”。 成都市公安局信访办提出，因案情复杂需要延期一个月答复，既然案情复杂，我要求四川省公安厅信访办举行听证会，以便双方查明事实，分清责任。<br />
