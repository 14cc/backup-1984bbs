艾未未：<br />
你于2009年12月10日，对成都市公安局作出的《公安机关处理信访事项答复意见书》[（2009）5号]不服提出的复查请求。经复查，现答复如下：经我厅认真复查，维持成都市公安局的调查结果。<br />
