谁，才是真的勇士。<br />
我只知道自己不是，还没有上新闻战场之前就缴械了。<br />
记者们的勇气让我敬佩，他们的真相被一次次地枪毙<br />
他们是这个时代最值得敬佩的人<br />
我的老师说，等我考完研，好好准备一下，推荐我去南方周末锻炼一下<br />
我哪敢告诉她，当年的那个信誓旦旦的男生，已经不断撤退<br />
退到底线，既不愿意回头，也不敢抬起<br />
