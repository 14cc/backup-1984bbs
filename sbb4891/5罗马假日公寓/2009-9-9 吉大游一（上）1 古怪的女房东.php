													<h2>吉大游一（上）1 古怪的女房东</h2>
						
						
																											<div id="postmessage_233998" class="t_msgfont">啊，这真是一个美丽的城市（名字），一年四季如春，可压根不会那么回事。从火车站出来的时候，虽然是早上五点，大学门口好多接学生的各校的高年级学生与老师？ 手持一个大牌子，喊着自己所在院校的名字。远处一个抓绒的矩形横幅在空中快要被撑成了一个面包状。红底金字――吉林大学。我当时有个错觉，这车就他妈接我的，我当时就想往车上蹭――转念一想，哥没通知书啊！别上去又被轰下来！还是感受感受没做过的小轻轨好！――但又一想，呸，让我做我都不做！出车祸咋办？我不是赔本了？其实我一早就有这个念头，你说一个学生从小努力努力学习，18年不窥园，考上清华北大，出门轰隆隆――――来了一辆大卡车。。。。<br />
<br />
&nbsp; &nbsp;你说他这一生多亏啊？ 我可不干那事儿，我聪明着呢。。。<br />
<br />
 <br />
<br />
&nbsp;&nbsp;做小轻轨到了前进大街，手持拉杆箱，背包，球包，一晃一晃到了南校北门，主干道上的宾馆，小包竟然要70？干脆把我卖了算了，看我值不值70（我在全国著名旅游城市――敦煌，住了两天，才30）万恶的资本主义市场经济！&nbsp;&nbsp;在前卫南校区的小北门，是距离学校最近的家属区，这里的一层大多被当地人变成了黑黑的小旅馆，我在问过看似老生后，到了这，可。。。。。（大包100，小包60）我操你妈，难道就没便宜的么？60在我们那不光能打一炮，还能送一碗正宗的烩面，里面人家还给你卧个――羊外腰（gaowan）。<br />
<br />
 <br />
<br />
&nbsp;&nbsp;在找过数家之后，我放弃了，新生在家长的带领下陆陆续续这几天都要到，是客源最多的时候，他们根本不愁租出去，一个老板说阳台可以住人――只要40，我欣然答应。<br />
<br />
 <br />
<br />
&nbsp;&nbsp;放好行李，背着包绕了校区一圈，好多打着迎接新生的大牌匾，进学校还要测体温。。根据我短暂观察，不需要出示录取通知书，我也感受了一把新生的待遇。。。。<br />
<br />
 <br />
<br />
 绕了一圈回来，隔壁是个材料系大三的长期租住的哥们，聊了一小会，一起吃了个饭。买了两杯扎啤，想请同学喝。。。。。。。。谁知道人家不喝酒。。。。我都给喝了。。。一杯2.5，还是玻璃杯，挺好的。。。。大学就是好。。。。回来睡了一小会。这天夜里是最难受的，东北的棉被很厚，我床下面又是海绵垫，盖上被子热，不盖被子盖我的毛巾被，冷，奶奶的胸。（这天晚上租房被忽悠了。。。人家都不找我合租。。。。狗日的，，）<br />
<br />
 <br />
<br />
30号早上开始找房子，新买的手机卡，要到9月1号才能开，这使得我的交流变的异常艰难。在晃悠了很长时间后，在广告牌附近，搭上了一个阿姨。。。他的向阳单间，要750一月。。。。这个阿姨家三室一厅，两个向阳，其中一个做了隔间，其中的一个隔间大概有4、5平米左右，一个月要400元。另外一个阴暗一点的房间需要600元。住400元的隔间的同学，31号上午要搬走了，住600元的同学，大概要10、1走。对于我这个喜欢阴暗的人来说，那个小阴间是在是太适合我了。<br />
<br />
于是和阿姨商定，31号入住小隔间，等到考研的同学走了，我就入住小阴间。。。。共计1600元，三个月。在我的提议与阿姨的既不情愿下。。她只少便宜了我20元钱，作为钥匙押金，实在是一个古板的阿姨。。。。我这么好的孩子。。。<br />
<br />
这个阿姨。。。和他老公住在厨房。。。实在是一个古怪的阿姨，提出了很多古怪的条件。<br />
<br />
 <br />
<br />
不能带女的回来住，不能带同学回来，不能带任何人回来。不能养宠物。。门要一直开着。。。等等很多。。。慢慢相处吧。。。希望能愉快点。。。<br />
<br />
从租住的地方，把行李拿出来，放到这个阿姨家，本来想和阿姨说，如果大房间晚上没人住，可以让我蹭一晚上的。。。结果阿姨义正言辞的拒绝，说要出租给别人，我想也是，不能耽误人家财路。可晚上回去拿东西的时候，阿姨就紧急走在我前面关向阳单间的房门。。。我清楚的判断。。里面么有人。。。这个阿姨，也不让我住。还好，反正要上网，就来了网吧，8元钱，开了到长春的第一个通宵。9:00-7:50<br />
<br />
下午经过咨询老鸟（他还给我说错了。。。），从前进大街坐222到宽平大桥，从宽屏大桥倒184到欧亚卖场，从欧亚卖场倒146到四季青市场，这个黑市二手车市场，12点半就散气了。。。。。我到的时候已经四点了，有位老板正好还有一辆山地车。。花200元买了一辆开价280的二手8.5新山地车，BARTE/BARTE？，我在网上也没查出来。。估计是个杂牌子。。还行。。。买了最好的锁25，一共230元钱。从四季青骑回宽屏大桥，共计45分钟。（真卖力气，这个小伙子不错，看准的姑娘快下手）<br />
<br />
然后找了网吧查了下 |三夫|户外用品的地址，去看了看户外用品，在同志街，西康街，吃了点小吃。骑车回南校区。。<br />
<br />
