<br />
高兴的回来开通宵上网了。。。。。<br />
<br />
 <br />
<br />
那个大三学材料的哥们，已经答应帮我“挂失”一张学生卡了，这样我就有幸可以蒙混进教学楼了。。。<br />
<br />
真的要谢谢他，希望，我的长头发，不要被老大爷大妈抓住。。。黑手保佑我。。。<br />
<br />
 <br />
<br />
 <br />
<br />
 <br />
<br />
 <br />
<br />
一会去洗澡，然后回去睡觉，晚上去长春理工大学看一看（想打羽毛球。。。）<br />
<br />
 <br />
<br />
 <br />
<br />
 <br />
<br />
到吉大两天，自行车，房子，学生卡，已经到手。（有意我的姑娘请速联系）<br />
<br />
 <br />
<br />
人生啊，美好的未来，开始了。呼呼。</div>

							
							
							
							
