<br />
=======================================================<br />
<br />
满意答案<br />
1.&nbsp;&nbsp;申请绿卡没有年龄限制<br />
2.&nbsp;&nbsp;申办的一般的做法是在美求学，拿到学位之后，寻求一份工作，由F－1学生身份转为H－1B受雇身份, 在美国境内，可以在从H-1B身份调整身份到职业移民, 经过雇主向劳工部申请劳工证。待劳工证批准后，向移民局提出I-140职业移民申请,申请批准之后，再递交I-485调整身份申请。申请获得美国绿卡。但每年全美只有65000个名额<br />
3.&nbsp;&nbsp;价格方面，要看你是自己办理还是找当地律师协助办理了。自己办理当然只是例行的费用（申请绿卡及指纹扫描的费用从目前是1010美元）但要是委托律师的话，各个公司的费用也是不尽相同的！<br />
4.&nbsp;&nbsp;申请绿卡之后，可以接父母过去探亲，绿卡持有3年之后，申请成为美国公民，才能帮父母办理移民。<br />
5.&nbsp;&nbsp;美国对持绿卡人士是没有所谓的“移民监”限制，即美国政府对获得永久居留权人士的居住地和在美停留时间没有做强制性的规定。但是，每半年必需返回美国境内一次，并与美国保持必要的关联，如每年正常申报纳税、有银行户口等等。如果持绿卡人士因为特殊原因无法在半年内返回美国，则需要事先向当地的领馆提出申请，否则将面临着被美国移民局取消绿卡的危险。<br />
6.&nbsp;&nbsp;拥有美国绿卡后，在美国国内基本享有和本国国民一样的待遇。<br />
比如一、可以永久居留美国；<br />
二、可以在美国工作；<br />
三、老年人可以申请“老年公寓”；<br />
