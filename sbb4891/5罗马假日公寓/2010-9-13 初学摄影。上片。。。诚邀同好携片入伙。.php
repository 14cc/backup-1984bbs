接触摄影 要学会看懂五本书<br />
2009-10-21 16:44<br />
摄影之所以被称为一门艺术，就是因为它需要一颗平静的心，任何浮躁与虚荣都是要不得的。 <br />
我相信，虽然进入了数码时代，但摄影的基础知识还是需要掌握的。在我看来，既然接触摄影，要学会看懂五本书。 <br />
<br />
<br />
第一本要认真看的书，当然是产品说明书！ <br />
<br />
在摄影论坛里，如果有人问产品说明书上本来很醒目的答案，是会遭到痛殴的。你只有在充分了解产品和基本操作的情况下，才能谈得上发挥系统的优点。也只有这样，相机才能真正成为你的工具。 <br />
<br />
<br />
第二本要看的书，是《纽摄》。 <br />
<br />
是的，很多人说，纽摄老了，没错，纽摄是老了，老了就抽抽了，抽抽得只剩下精华了。人们对照片有不同的追求，咱们不说，追求亮丽明快，或许纽摄不适合，但我看来，它仍然是摄影圣经。开篇三个要求，主题、主体、简洁，自始至终都是我们评判片子的依据。看明白了纽摄，相机就会成为你的左右手。你会开始为光影而喜悦，为色彩而快乐。 <br />
<br />
<br />
第三本要看的书，就是感情。 <br />
<br />
太浮躁的人是用不好相机的，因为他看见的只有服装和表情，他拍到的是漂亮或者丑陋，而不是喜欢或者厌恶；拍到的是笑容或者眼泪，而不是快乐或者悲伤。那样拍的片子，我们叫快门机器。单纯拍片子不是摄影，更不是艺术，摄影是藏在片子后面的感情！很遗憾，大部分人都还在看这本书，我也是。但是知道了方向，我想我们会突破的。等到突破了这一步，相机就能成为你的感情，你能很容易在打开一张片子的时候，迅速回到当时的现场，伴随一杯茶，半包烟，和一个宁静的下午。 <br />
<br />
<br />
第四本要看的书，是人生。 <br />
<br />
太年轻的人也是无法理解摄影的真谛的。没错，现在市场上的相机种类繁多，也不乏索尼T200一类的时尚机型，但是，如果把摄影定位成sony那种时尚，那就错到家了。摄影不是时尚，在这两个字后面蕴含着前辈们多少年的积累和精华，从取景框看出去，转动对焦环，在景物开始清晰的同时，你能隐约看见大师的身影飘然而过，翩若惊鸿，矫若游龙。镜头表达的，是你眼中的世界，是你对人生的理解。摄影大师们，在拍风光的时候，他会充满了敬畏；在拍花鸟的时候，他洋溢着喜悦，在拍人文的时候，他骨子里刻着悲悯。镜头便是人生，你的片子便是你对大千世界的理解，走到这一步，镜头就是你的伴侣，你已经感觉不出它的存在，但它确实开始融进了你的生命。 <br />
<br />
<br />
第五本要看的书，便是你自己的心。 <br />
