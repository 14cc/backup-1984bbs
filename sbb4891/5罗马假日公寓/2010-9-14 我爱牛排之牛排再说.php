													<h2>我爱牛排之牛排再说</h2>
						
						
																											<div id="postmessage_828768" class="t_msgfont"><img src="http://i53.tinypic.com/15nr474.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
大叔以前写过很多篇有关牛排的文章，今天老话重谈，旧屁重放，再说牛排。<br />
<br />
首先来了解一下最常见牛排的分类及特点。<br />
<br />
牛排STEAK种类非常多，常见的大概有这么4种：（以下牛排分类引自网络）<br />
-------------------------------------------------------------------------------------------------------------------------------------------------<br />
【RIB EYE 肋眼牛排】也叫肉眼牛排，是牛靠近胸部的肋肌肉，由于这个部位很少运动到，所以肉质很嫩，大理石纹较多分布均匀，肋眼牛排的中心部位有一块明显的油花，煎制时一定要烤到七、八分熟，把油脂烧透，散发出牛油香味来吃才是最美味的~<br />
<br />
【SIRLOIN 西冷牛排】也叫沙朗牛排、纽约客NewYorkStrip（牛外脊，牛的后腰肉），含一定肥油，由于是牛外脊，在肉的外延带一圈呈白色的肉筋，肉质细嫩，上好的沙朗会嫩到入口即化的地步。西冷牛排要比菲力牛排多一些韧性，所以入口更有牛排的芬芳。吃的时候以五分至八分熟最好，适合年轻人和牙口好的人，最好不要煎得过熟~<br />
<br />
【FILET 菲力牛排】（TENDERLOIN 嫩牛柳，牛里脊，腰内肉），是由臀部及腰肌肉取下的一块软肉，是牛身上运动量最少的一块肌肉，所以特别柔嫩，其大理石纹脂肪较少，瘦肉较多油脂含量低。<br />
<br />
煎成3成、5成和7成熟就可以咯。一般做菲力牛排时都会切得较厚，所以在肉厚油少的情况下，火候拿捏显得格外重要~<br />
<br />
【T－BONE T骨牛排】呈T字型，是牛背上的脊骨肉。T型骨头两侧的肉一边量多一边量少，量多的是西冷，量稍小的便是菲力~ 选择T骨牛排可以说是一举两得，既可以品尝到菲力牛排的香嫩又可以感受到西冷牛排的芳香。<br />
---------------------------------------------------------------------------------------------------------------------------------------------------<br />
目前市场上的牛排基本都是国产牛，各西餐厅供应的也大都如此。市场里的供应商总会不停地告诉我那家西餐厅的牛排是我供应的，那家西餐厅从我这里进了多少牛排，所以哪家西餐厅如果告诉你牛排从国外漂洋过海而来，卖诸如澳洲进口美国进口日本进口的噱头，你大可一笑置之。<br />
<br />
你别奢望那是真正的进口牛排，也没有必要花昂贵的价格去尝一块不知从何而来，不知新鲜度如何的牛排，作为普通人还是就地取材，经过大厨的加工，也能品尝不同凡响的牛排。<br />
<br />
为什么有的牛排爽嫩细滑，有的牛排味如嚼蜡呢？我们普通人怎么才能吃到可口又便宜的牛排呢？其实很简单，你就可以做到。<br />
<br />
首先从市场买来新鲜的牛肉，你喜欢吃什么部位就买什么部位，然后进行分切。分切的方法是牛排爽嫩的第一步，把牛肉挑筋去膜，以半公分左右的厚度横切，切记是横切。<br />
<br />
然后是进行排酸。新鲜的牛肉都有很大的酸味，如果不排酸吃起来非常影响口感，一般市场里分切好的牛排厂家基本都已经排过酸，如果新鲜的牛肉要排酸，大叔告诉你一个最简单最容易的方法，就是放进零下20°左右的冰柜急冻，这样酸度基本可以排除。<br />
<br />
排酸以后的牛排就可以腌制了。国产的牛排必须经过腌制，一个西餐厅如果有一个懂腌制的大厨，那这家餐厅的牛排成功了一大半。不懂腌制的大厨会采用大量的嫩肉粉和食粉来使牛排变嫩，但是大叔要说这样腌制出来的牛排不但口感糜烂没有爽滑弹嫩，而且会伤害身体。基本上可以说是失败的大厨。<br />
<br />
那么如何腌制出爽滑弹嫩不带任何添加剂的牛排呢？腌制的过程这里就省略，保密。<br />
<br />
把腌制好的牛排分别用保鲜膜包裹，然后放入冰箱冷冻，想要吃的时候就拿出一块，让它自然解冻，不要用水冲，也不要用微波炉解冻，如果用水冲就冲掉了牛排固有的鲜味，如果用微波炉解冻，一不小心就把牛排表面打熟，这样煎出来的牛排就跟柴爿没有任何区别。<br />
<br />
那么如何煎呢？一般家庭里没有西餐厅专用扒炉，可以采用平底锅煎制。但是家庭用火无法和餐厅所用的扒炉相比，温度很难掌握，所以对火候的掌握就有点难度，不过也没有关系，大叔告诉你怎么煎。<br />
<br />
首先锅烧热，倒少许色拉油，温度到八九成热的时候，把牛排平放入锅，快速高温双面封汁，这时的牛排基本已是三成左右，如果你喜欢生一点，这样就可以了，如果你喜欢熟一点，就捞出牛排，关小火头，再放进牛排进行两面加熟。<br />
<br />
不过，切记时间还是不要过长，牛排的好吃在于生度。<br />
<br />
西餐中牛排对于生熟度的讲法一般为三种：rare、medium、well，没有中国人所说的几成熟，不过也可以对应为rare就是三成熟，medium为五成熟，well为七成熟。<br />
<br />
最后就是搭配盘头，就是搭配些蔬果之类的东西，一是更加美轮美奂，二是营养更加均衡，三是减少油腻。如果你喜欢，可以搭配你中意的蔬菜或水果，没有死板的定式。<br />
<br />
浇上你想要的汁，一般都采用黑胡椒汁和番茄沙司，当然也可以用洋葱汁等等，最简单又拿范的就是直接撒上黑胡椒碎，自己动手用一个木制的研磨器挤上黑胡椒碎，那真叫一个范儿。<br />
<br />
铺上餐巾，拿起刀叉，一刀下去，塞入口中，不出声的咀嚼，是不是觉得自己身份倍儿增？<br />
<br />
如果喜欢，那就赶快行动，奔向咖啡馆，这个小资聚集的地方，每天帅哥美女云集，有萝莉，有熟女，有小生，有老男，总能遇到你想要的人，去点上一杯cappuccino，去买上一块牛排，体验不一样的口舌之快。<br />
