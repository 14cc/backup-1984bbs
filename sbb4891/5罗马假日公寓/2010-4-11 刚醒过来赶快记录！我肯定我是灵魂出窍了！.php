&nbsp; &nbsp; 出梦。就是走出梦境，从梦境中走出来有两种可能，一是可能会醒，二是进入另外一种境界。出梦要求的是不从床上醒来，而是阴神自醒。<br />
<br />
&nbsp; &nbsp; 实境。出梦之后阴神自醒，要判断是否达到这一境界，就是要看梦中的自己是否已经脱离梦境走入实境。也就是以另一种形式回归现实。梦中所见一切不再是梦境，而是现实中的实景。<br />
<br />
&nbsp; &nbsp; 明境。阴神与识神不同，眼中所见常常是心像或幻像。由梦入实境之后需要经过一段时间的修炼，明辨实景。再这个过程中，就会有神通，比如说像修道人的天眼一样，可以透视实物，但这种神通只能在梦中拥有。<br />
<br />
&nbsp; &nbsp; 神境。这是大多数人修炼入梦大法或类似法术所能达到的最高境界。梦中可以离地飞行、可以穿墙过壁，可以瞬间移动。总之神话传说中神仙有的种种神通在神境中都会拥有。相当于在梦中得到五眼六通中的神境通。不过这不是真正的神境通，只在梦中而已，不会对现实世界造成任何影响。</div>

							
							
							
							
