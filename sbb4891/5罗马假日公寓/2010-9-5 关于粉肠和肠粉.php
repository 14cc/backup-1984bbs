													<h2>插播一个自己偶尔做的美食</h2>
						
						
																											<div id="postmessage_815367" class="t_msgfont">叉烧饭<br />
<br />
直到上大学才第一次吃叉烧饭，在学校食堂，很失望。后来来北京，在金鼎轩、广东人民、日昌等等吃了几次叉烧饭，觉得还可以。2010年春节在香港时代广州附近一条街的一个小店里吃了一次叉烧饭，这一次很难忘，并一直回味。<br />
<br />
北京饭店里的叉烧饭不便宜，又难吃，为了解馋，必须自己研究：<br />
<br />
米饭，最好是泰国香米，没有的话尽量选用南方米。电饭锅煮熟，我爱吃硬口的，所以水少放，我建议你也少放，因为米太粘影响整体口感。<br />
<br />
配料：鸡蛋，煮一个。<br />
<br />
配料：榨菜，少许，调味。<br />
<br />
配料：油菜或者菜心，水焯熟。<br />
<br />
这些都是辅助的。接下来说叉烧肉：<br />
<br />
猪里脊一块、李锦记叉烧酱。<br />
<br />
通常把肉涂上厚厚的叉烧酱腌24小时最好，但也可以快速做好，我通常的方法是：<br />
<br />
热锅，放入一些水，放入够量的李锦记，放入切成片的猪里脊。让汤汁淹没肉。让它接近收汁一次，再放水，再来一次收汁（不要完全收完，如果肉颜色很深了，留一下一会儿倒入饭里），整个过程火中火或以下。注意，因为含糖，所以锅因此会很难刷，不过为了一次美味也值得了。<br />
<br />
把米饭盛入碗内，把鸡蛋（切半，你和你爱人或亲人一人一半）、油菜 、少许榨菜放在米饭上铺好，叉烧肉铺好。把剩余的一点汤汁浇在叉烧肉上。</div>

							
							
							
							
