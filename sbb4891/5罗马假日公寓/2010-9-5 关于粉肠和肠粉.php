													<h2>关于粉肠和肠粉</h2>
						
						
																											<div id="postmessage_814956" class="t_msgfont">今晚有组员问我：粉肠和肠粉是不是相同的东西？我碰到这样的问题已经有四、五次了，所以想说一下。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;粉肠和肠粉不是同一样东西，但都是食物，主要作早餐和宵夜。我表达能力不好，所以上网搜索了别人的说法。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;首先说粉肠，粉肠是淀粉与各种调料混合后灌入猪小肠蒸熟的食品，应该是猪小肠的，我还没有吃过猪大肠的，所以小组ID为猪大肠的兄弟可以淡定。我们一般将粉肠滚粥，味道很好的（我30分钟前才吃过）。“粉肠”是骂人的词语，周星驰的电影出现次数很多。有网友说：“因为香港人和广州人一样,喜欢吃粉肠.特别煮粥时.放到粥里一烫就熟.而且是爽口弹牙.显而易见.你条粉肠还不让我吃定了?这句话大概是说对付你很容易的意思. ”也有香港人说：“我是老香港, 让我来说两句. 这话源起于30年前. 这本来是句粗语--&quot;X样&quot;. X是男性的小弟弟. 当时一般在写字楼做的男白领比较斯文, 不好意思说粗话, 就用了近音&quot;粉肠&quot; 来代替, 流行至今. 现在一些比较粗俗的人还直接说&quot;你这个X样!&quot;我比较相信香港人的说法，因为被人骂”粉肠“的，都是男性。引申开去，也有人会骂”粉人“，意思应该和”粉肠“差不多，星期五我才被组员地中海月光这样骂过。其实，我觉得”粉肠“并不是骂人的，”你条粉肠“意思和”你这个家伙“差不多。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;再说肠粉：广东肠粉主要流派有两种:一种是布拉肠，另一种是抽屉式肠粉，由于使用肠粉的制作工具不同，所以导致做出的肠粉都不相同，布拉肠粉是以品尝馅料为主（肠粉浆大部分是使用粘米粉再添加澄面,粟粉和生粉），而抽屉式肠粉（肠粉浆是使用纯米浆做成）主要品尝肠粉粉质和酱汁调料！(百度百科)肠粉可以说是广东最流行的早餐，如果酒店没有肠粉，就不要说有早茶饮。我知道小组有很多人喜欢吃，上肛上腺就一直对肠粉念念不忘。还有一个小天使说他不喜欢，不过根据他的描述，我断定他吃的不是肠粉，而是其他食物。我到过很多地方吃肠粉，比较喜欢的有广东省云浮市河口肠粉（几年前去过的，不知道现在还在不在），河口肠粉是属于抽屉式肠粉，粤西那边流行，有一个师傅叫老卡（经查，非本小组的老卡）。还有就是广州东川路的穗银记肠粉店，去年我在广州住了一段时间，天天跑去吃。来广州的朋友，有机会一定要去穗银记肠粉店试试。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;如果谁还有更好的说法，请说一说。知道有更好的店，也分享一下，我想去试试。</div>

							
							
							
							
