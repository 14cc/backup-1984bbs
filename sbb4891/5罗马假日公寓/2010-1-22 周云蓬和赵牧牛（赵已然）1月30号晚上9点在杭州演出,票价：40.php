赵已然介绍：<br />
别名赵牧牛、赵老大。男，约40岁，宁夏银川人，北京老一代摇滚鼓手，在走穴风潮和摇滚运动中成长，并选择了最卑微的生活方式。近年来曾担任木推瓜等新乐队鼓手。极少开口的超级歌手。 <br />
他的歌声，无论翻唱还是创作，都带有浓重的布鲁斯和西北民歌风格。在这方面，迄今为 止，尚未有过任何华语歌手达到过他的境界――让布鲁斯和民歌融合得天衣无缝，以至于你宁可相信布鲁斯就是西北街头小伙、六盘山下农民流传的声音。他的演唱 和吉他技巧同样民间、随意而又堪称精湛，带有浓烈的沧桑情怀，所谓人生百味，催人泪下，莫过如此。此情可待成追忆，只是当时已惘然，赵已然的80年代老歌 不但把他自己的青春变成一场无可挽回的旧梦，也足以凝聚更多人的悲欢离合，将苦酒和热泪融化成叹息。 （文/颜峻）<br />
 <br />
赵已然自白： <br />
活在1988 <br />
我本该是一名化学教师，阴差阳错，不幸做了鼓手。十多年来，不求上进，碌碌无为，混迹于狭小的地下音乐王国，沉迷于越来越糊涂越来越荒唐的卡通境地，信以为真地在有限的几位朋友面前义正辞严、斩钉截铁地鼓吹着“垮到极处”的寄生虫哲学。从没有过工作，后以借钱为生。 <br />
后来，我慢慢变成了一个人。只有一双拖鞋、一只牙刷，住在了农村，且越搬越远。 <br />
再后来，我笑得有些难看了，因为我越来越没钱。以至于常常被迫求告家人，艰难度日。 <br />
有一天，我终于发现，磕不动了，再也垮不下去了。我头天让酒喝醉，吐了；第二天一早，酒还没醒，咣叽，又让茶给喝吐了。 <br />
那一天，我发现，我的脸特别难看，太难看了。我终于知道，我太不漂亮了。 <br />
我一生热爱漂亮女人，痴情于不敢面对、不敢亵渎的漂亮女人，然而我自己却从没漂亮过，从没漂亮过一次。 <br />
我也知道了，在我所追求的自由中，我没有自由过一次。 <br />
于是，我终于倒下了。 <br />
于是，在深夜里，在不要钱的灿烂阳光下，在只有神或鬼才能看得见的微笑或悲痛中，我想起了那些曾经会唱的歌。 <br />
于是今天，被逼无奈，我端正了思想，换了身份，不做鼓手，稍不情愿地自觉有些滑稽般地坐在了这里，怀着年轻时代的美好梦想，准备唱歌。 <br />
