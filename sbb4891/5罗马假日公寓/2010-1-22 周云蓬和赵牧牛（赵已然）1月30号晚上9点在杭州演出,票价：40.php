周云蓬介绍：<br />
1975年，来北京就医一个月；1987年，来京求学未果；1995年，沈阳一朋友盛赞圆明园画家村，周云蓬辞职带上600元钱来京，住进了画家村，一是因为这里艺术青年聚居，二是因为房租便宜；1996年初，在北大南门图书城一带卖唱。出发前背起吉他，扛上音箱，卷一张大饼。唱罗大佑、还有叶佳修，也唱“LetItBe”。与“同村”的艺术家相比，周云蓬收入不菲，半书包的零钱背在身上更有腰缠万贯的感觉；1997年，在北京的某酒吧办蓝色老虎沙龙，周末朗诵、演唱；1998年，在昆明游山玩水，10月回北京，开始在诸多音乐主题酒吧演唱；1999年，创办了民刊《命门》；2000年，去西藏，被朋友拉扯上了6000多米的雪山；2001年，办民刊《低岸》，登载诗歌、小说等。2004年9月，《沉默如谜的呼吸》正式发行，单曲《我听到某人在唱一首忧伤的歌》收录在同时发行的《Badhead3-花园》中。2007年因未通过审查，专辑《中国孩子》地下发行。<br />
 <br />
周云蓬自述：<br />
蛇只能看见运动着的东西，狗的世界是黑白的，蜻蜓的眼睛里有一千个太阳。很多深海里的鱼，眼睛蜕化成了两个白点。能看见什么，不能看见什么，那是我们的宿命。我热爱自己的命运，她跟我最亲，她是专为我开，专为我关的独一无二的门。<br />
　　某些遥远的地方，一辈子都不可能去。四川有个县叫“白玉”，西藏昌都有个地方叫“也要走”，新疆的“叶尔羌”，湖南的“苍梧”，这些地名撼人心魄，有神态有灵魄，在天之涯海之角他们有隐秘的故事，殷勤地招呼我过去听。但人生苦短，我大概没有时间听所有的故事，如果今生无缘，那就隔着山山水水握一握手。<br />
　　走在街上，想唱上一句，恰巧旁边的人唱出了那句歌。是什么样的神秘的力量抓住了两颗互不相识的心？音乐是游荡在我们头上的幽灵，它抓住谁，谁就发了疯似的想唱歌，可我怎么才能被它永远抓在手里？我走遍大地或是长久地蜗居一处，白日纵酒黑夜诵经，我呼喊音乐，把我从我的现实生活中拔出来，但常常落空，我只有埋头于生活里，专注地走一步看一步。音乐不在空中，它在泥土里，在蚂蚁的隔壁，在蜗牛的对门。当我们无路可走的时候，当我们说不出来的时候，音乐，愿你降临。<br />
　　――周云蓬于香山<br />
