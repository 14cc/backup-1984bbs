													<h2>到底是谁散布了吃唐僧肉可以长生不老的谣言?</h2>
						
						
																											<div id="postmessage_455279" class="t_msgfont">看西游记，有一个很奇怪的疑问：为什么会有吃了唐僧肉可以长生不老的说法，这谣言是怎么在妖精中间传开的。<br />
<br />
唐僧同志也是按正常人的投胎程序来到人间，在生理结构上应该没有什么特殊之处。再参考泾河龙王被杀、他的老婆病死，猪八戒、沙僧也差点丢掉性命，可见神仙也会死，吃了唐僧肉可以长生不老简直就是一派胡言。退一步，就算吃了唐僧肉可以长生不老，妖精们又怎么知道这消息呢？要知道，唐僧的父母并没有做广告说自己生出了肉如何如何好的儿子，况且西天路上的妖精因为利益关系，基本上老死不相往来甚至势不两立的。像第一个传说吃了唐僧可以长生不老的妖精白骨精，一辈子独来独往，看不出有什么朋友。妖精甲知道吃了唐僧肉可以长生不老，又不想合伙把唐僧吃掉，总不会跑去告诉和自己素无往来的妖精乙吧。要知道，从长安到西天有十万八千里，是长征路的好几倍，凭着妖精们这么窄的社交圈子，无论怎样，都不可能把吃了唐僧肉可以长生不老的消息传遍西天路上的妖精。<br />
<br />
这样，可以推断出，是有人故意传播吃了唐僧肉可以长生不老的谣言。而且这个人一定很了解取经的路线，甚至对妖精的活动都很了解才行。否则，你根本上不知道妖精住在哪里，怎么把消息传给他呢？<br />
<br />
那么，到底是谁在制造谣言，传播谣言呢？<br />
<br />
分析对取经时间、路线最清楚的人，除了发起人如来，唐僧师徒，就是观音，还有陪同唐僧取经的六丁六甲、五方揭谛、四值功曹、一十八位护教伽蓝了。<br />
<br />
如来、唐僧师徒制造这谣言的可能性几乎为零，做人，怎么能跟自己过不去呢？<br />
<br />
另外一个制造谣言的人可能是观音，作为考核干部的具体操作人员，观音始终关注取经的一举一动。对唐僧的考核，可谓观音的样板工程，政绩工程。让这事情做的热闹一些，产生轰动效应，观音面上也有光，所以表面上看，她可以有这样的动机。<br />
<br />
但仔细一想，观音制造谣言的可能性极小。虽然说唐僧内定的干部，考核只是走过场戏，但也不是那么容易通过的。就像现在一些明明是领导看中的干部，却偏偏想经过民主选举，一民主，结果就被差额掉了。同样在考核唐僧过程中发生的事情，也让观音捏了多少把汗。和猴哥结下梁子的太上老君就派秘书金角童子和银角童子、司机青牛精来和猴哥过不去。就算在西天内部，因为看不惯如来随便提拔自己的亲信，弥勒佛也派出秘书黄眉童子弄出一座小雷音寺来，让唐僧出出丑。一般来说，天上下来的妖精好说，第一他们毕竟是见过世面的，不会相信吃了唐僧肉真的会长生不老。第二就算他们敢不要公务员这铁饭碗，也知道人民**的专政工具不是开玩笑的。他们的领导尽管和如来面和心不和，却没有彻底闹翻的意思。所以尽管他们对如来提拔唐僧横挑鼻子竖挑眼，百般刁难，却光棍劈竹不伤笋，做事很有分寸。正因为如此，这些妖精们可以让唐僧出出丑，却绝对不会一口把唐僧吃掉，否则麻烦也大了，吃了如来的人，如来会放过他吗？尽管如此，观音也不太放心。针对天上神仙的一些新式武器，观音特意给了猴哥三根保命毫毛。观音如此小心翼翼，也有发生意外的时候，莽撞猴哥就一不小心打死了玉皇大帝这一派系的人碧波潭龙王。幸亏玉皇大帝知道自己斗不过如来，而且万碧潭龙王只是一个基层公务员，大不了给他一个因公殉职，追认为烈士拉倒，不至于造成很大的麻烦。<br />
<br />
可见表面上看起来这是十拿九稳的事情，实际操作起来中间不可控的因素太多，一不小心就搞得收不了场。<br />
<br />
组织上的考察还好说一点，民间的妖精就更难弄了。他们又不在如来、玉帝那里拿工资，没有什么组织纪律，初生之犊不怕虎，人间的妖精，哪里知道天堂的种种规矩？抓到唐僧，一口就把他生吃了，到时你再跟他们说八荣八耻吧？不过还好，这些民间的妖精一般武功不是特别高，结果还没有来得及吃唐僧就被制服甚至打死了。<br />
<br />
还有一种是踢开*委闹革命的妖精，这些妖精是王八吃了秤砣，铁了心造反的。放倒如来，夺过他的雷音宝刹这样的豪言壮语都敢说，吃一个唐僧，当然不在话下。不过天见可怜，这样的妖精并不多。整个西天路上，只有狮驼洞有几个这样的妖精，被如来制服了。如果唐僧真的让妖精吃掉，叫观音怎样有脸见如来啊？<br />
<br />
那些想和唐僧一夜情的女妖精，估计也让观音够头痛的了。这些妖精们虽然开放，但无论怎样说毕竟是女同志，不会对唐僧八王硬上弓。不过，如果唐僧自己把持不住，总不能怪别人吧。虽然现在男女关系不算什么新闻，但如果在如来考察亲信的过程中发生这样的事情，也让观音颜面扫地的了。<br />
<br />
现在，上托老天爷的鸿福，下靠几个哥们的努力，总算没闹出什么乱子来。但是，观音原来以为三四年取经就可以完成的，结果花了十四年，难度远远超出她的想像。如果她还要制造出谣言来，让多几个妖精在西天的路上闹一闹，我是怎样说也不相信的。<br />
<br />
六丁六甲等几位无疑是吃了大亏的，跟着唐僧去取经，暗中保护唐僧吃尽了苦头，却不能出半点凤头。最后不但没有得到半点封赏，甚至也没有得到顾全大局，甘为人梯这样的官样评价。就算陪太子读书，陪成这样子也算背的了。说他们心中没气那是假的，不过如果说他们希望唐僧被妖精吃掉，那就冤枉了。组织上信得过，如来看得起的干部唐僧同志在考察的过程中如果有三长两短，六丁六甲等天庭的小萝卜头第一个就被抓出来做替罪羊。所以，如果说他们传播谣言是不可信的。<br />
<br />
那么，除了上面这些仁兄，还有谁有条件散布吃了唐僧肉可以长生不老的谣言呢？<br />
<br />
可以看谁经常出现在西天的路上，那就是太白金星了。<br />
<br />
去取经，和太白金星应该没什么关系。自始至终，观音都没有委托他对唐僧同志进行考核，不过他老兄却十处敲锣九处在，经常在不应该出现的地方见到他。第一次，从一些山精野怪中把唐僧救下来。第二次是告诉猴哥制服黄凤怪的方法。在他这次出场后，就从白骨精口中传出了吃唐僧肉可以长生不老的谣言。第三次在车迟国，托梦给一些受苦的和尚，说猴哥会解救他们。第四次他在狮驼洞前，给猴哥他们报信。第五次猴哥告托塔李天王的时候，他出来做和事佬。第六次猴哥和三个犀牛精交手的时候，他推荐四木禽星来收拾犀牛精。<br />
<br />
不过太白金星的上镜率虽然高，好像却没有给猴哥他们帮很大的忙。尤其是该出手的时候不出手，唐僧最可能被妖精吃掉的时候，如落在红孩儿、南山大王手中的时候，反而不见他的影子。比如他虽然从山精树怪重把唐僧救下来，但是不要忘记，唐僧是有六丁六甲、五方揭谛、四值功曹、一十八位护教伽蓝保护的，就算不救，这些武功平平的家伙夜奈何不了唐僧。推荐人来制服黄凤怪，告诉猴哥狮驼洞有妖精倒算是真的帮忙。不过别忘记黄凤怪和西天有关系，狮驼洞的三位妖精以前也在灵山工作过。虽然前者其实还没有找到组织，后者已经决心出去自己捞自己煲了。但如果让不明真相的外人来看，还以为他们都世灵山派下来考核唐僧的干部。托梦给车迟国的和尚，在猴哥和托塔利天王的纠纷中充当和事老，怎么说都像在送免费人情。推荐四木禽星来收拾犀牛精是事实，但是犀牛精过去只是偷油，并不吃人的。唐僧过来，他们才扬言要吃人。而且，猴哥上天找救兵的时候，就碰到他刚好从外面回来，和传达室的几个天师在那里聊天。并且，他对三个犀牛精的底细知道得这么清楚。所以，传播吃了唐僧肉可以长生不老的谣言，太白金星是有很大的嫌疑的。他推荐推荐四木禽，也许就是位了清洗嫌疑。<br />
