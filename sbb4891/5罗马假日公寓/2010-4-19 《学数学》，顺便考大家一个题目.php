																											<div id="postmessage_571136" class="t_msgfont">　　　　　　　　　　　　　　　　学数学<br />
　　　　　　　　　　　　　　　・曾国平・<br />
<br />
<br />
　　儿子今年上小学四年级。由于他不让我在家教他数学，我只好将他送到一所中文学校上周末数学提高班啦。正好，他老师是一个心平气和的中国太太，和我也比较熟。教材是给新加坡六年级学生用的，比美国六年级的数学教材难很多。<br />
<br />
　　一天，他拿来一道家庭作业让我帮忙。我习惯性地大叫一声：<br />
<br />
　　“你怎么搞的？连这么简单的题都不会做啊。”<br />
<br />
　　“那你试试看！”儿子反驳道。<br />
<br />
　　我拿起那道题一看，发现它果然不简单：“一个正方形ＡＢＣＤ，边长为１。分别以Ｃ和Ｄ为圆心做单位园相交于正方形内的点Ｅ。试计算线段ＡＤ、弧形ＡＥ和ＤＥ所围图形的周长和面积。”<br />
<br />
　　周长还好办，只要用对称性即可。面积可让我头疼啦，我左思右想也找不到一个比较简单的办法。最后，我不得不用初中平面几何和勾股定理的知识才找出了答案。另我吃惊的是，答案中有根号。于是，我赶紧给他老师打电话问究竟：<br />
<br />
　　“这道题不适合小学生做。原因是答案中有根号，而小学生是根本不懂根号的。”<br />
<br />
　　“你说得不错。已经有几位家长打电话来抱怨这道题太难，连他们都做不来呢。”她心平气和地回答说。<br />
<br />
　　“这么难的题目以后还是少布置吧！”我笑着建议说。<br />
<br />
　　“不行啊！要是题目太简单了，家长也会抱怨。”<br />
<br />
　　“在美国做数学老师真难啊！”我深有体会地附和着她。<br />
<br />
　　“班上有些小孩才上三年级，上我这门课很吃力。可是，他们的家长硬要逼着他们上啊。”<br />
<br />
　　“这不是拔苗助长么？”我反问道。其实，我儿子本来也该上三年级的。<br />
<br />
　　挂掉电话后，我思绪万千。我住在德州一个大都会，华人有十几万。这儿的华人家长对数学和钢琴热情如火。他们自己的音乐细胞都不多，好像要从孩子身上补回来似的；他们的数学细胞不少，好像要从孩子身上找出来似的。每次数学竞赛都像一次华人大聚会。小孩在里面轻松地比赛，家长们则站在外面紧张得拼命聊天。当宣布成绩的时候，要是自己的小孩得了奖，平时不善亲待孩子的父母也会跑过去笨拙地亲着小孩；要是自己的小孩没有得奖，父母会拉着小孩赶紧溜掉，怕熟人问起来丢人。<br />
<br />
　　我不大敢带小孩去参加华人举办的数学竞赛。我太想让他们赢，偏又怕他们输。我对他们的期望太高，所以往往会失望。我平时讲究绅士风度，从不对人发脾气。不过，一牵涉到数学，我就会变得粗鲁和粗暴。我一直想自己教两个小孩学数学，但效果都不大理想。要么小孩不听我的，要么我把他们骂得大哭。我的口头禅是：“你们怎们这么笨啊？”我常常教训他们：“我象你们这么大的时候早就会做这种题啦。”其实，我在给别人辅导数学的时候，脾气和态度可好啦。也许是钱在作怪吧，教自己的小孩不但不赚钱，反而要赚气啊。也许是我的爱心不够吧，毕竟，我极少拥抱他们，也极少对他们说：“我爱你！”总之，我一生无法跳出数学这个坎。数学是我一生的凤，数学是我一生的洞，数学是我一生的梦，数学是我一生的痛。<br />
<br />
　　数学作为一种职业还是近代的事。随着十六世纪大专院校在欧洲的涌现，数学变成了一种高贵的职业。其实，早期的数学家很多都是业余的，他们大多是富家子弟，都喜欢玩弄比少女还纯洁的数学。<br />
<br />
　　对数的发明者约翰・纳皮尔（１５５０－１６１７）出身于英国一个贵族之家，是Ｍｅｒｃｈｉｓｔｏｎ城堡的第八代传人。他一生荣华富贵，未曾有过正式的职业。他年轻的时候正值欧洲掀起宗教革命，他写了大量的文章攻击旧教（天主教），成了赫赫有名的神学家。当天主教的西班牙扬言要派无敌舰队攻打英国时，纳皮尔开始研究兵器（包括炮、装甲马车、潜水艇等）。虽然在他的兵器制成以前，英国已把西班牙的无敌舰队击垮，他还是成了英雄人物。<br />
<br />
　　他利用业余时间研究数学，以发明对数运算而著称。那时，天文学家们做了很多的观察，需要算几个数的连乘，因此苦不堪言。１５９４年，他发明了对数方法，从而解决了天文学家的难题，这让他在数学史上占据了很重要的一席之地。此外，他还发明了纳皮尔尺，这种尺子可以机械地进行数的乘除运算和求数的平方根。<br />
<br />
　　费马（１６０１－１６６５）堪称“业余数学家之王”。他出生在法国一个贵族家庭，上大学的时候选了当时最时髦的法律专业。他大学尚未毕业便买好了“律师”和“参议员”的职位，毕业后，他很顺利地当上了图卢兹议会的议员。以后几年，他官运亨通，越做越大。然而，费马没有什么领导才能，其官场生涯没有什么突出政绩。不过，费马从不利用职权向人们勒索、从不受贿、为人敦厚、公开廉明，赢得了人们的信任和称赞。<br />
<br />
　　对费马来说，真正的事业是数学。他通晓法语、意大利语、西班牙语、拉丁语和希腊语，语言方面的博学给费马的数学研究提供了语言工具和便利，使他有机会真正了解阿拉伯和意大利的代数以及古希腊的数学。在数学上，费马不仅可以在数学王国里自由驰骋，而且还可以站在数学天地之外鸟瞰数学。<br />
<br />
　　费马一生从未受过专门的数学教育，数学研究只不过是他的业余爱好罢了。然而，他是公认的有史以来十位最伟大的数学家之一。他是解析几何的发明者之一，概率论的主要创始人。他是数论的绝对权威，对于微积分的贡献仅次于牛顿、莱布尼茨。<br />
<br />
　　１６３７年，费马提出了著名的费马大定理：“不可能把一个整数的立方表示成两个立方的和，把一个四次方幂表示成两个四次方幂的和，一般地，不可能把任一个次数大于２的方幂表示成两个同方幂的和。”他在一本书的下面作了批注：“这个定理的证明太简单以致我不想在此浪费笔墨去证它。”正是这个费马大定理让数学家整整忙了３５０年才证明了它是对的。<br />
<br />
　　德国数学家哥德巴赫（１６９０－１７６４）也是业余数学家。他曾在英国牛津大学学法学，后来对数学产生了浓厚的兴趣。毕业后，他改做了中学数学老师，并利用业余时间研究数学。１７２９年至１７６４年，哥德巴赫与大数学家欧拉保持了长达三十五年的书信往来。在１７４２年６月７日给欧拉的信中，哥德巴赫提出了如下猜想：<br />
<br />
　　（ａ）　任何一个＞＝６之偶数，都可以表示成两个奇质数之和。<br />
　　（ｂ）　任何一个＞＝９之奇数，都可以表示成三个奇质数之和。<br />
<br />
　　这就是着名的哥德巴赫猜想。其中，（ａ）的简称为“１＋１＝２”。<br />
<br />
　　欧拉在６月３０日给哥德巴赫的回信中说：“我相信这个猜想是正确的，但却不能证明它。”２００年过去了，还是没有人能证明它，哥德巴赫猜想由此成为数学皇冠上一颗可望不可及的“明珠”。人们对哥德巴赫猜想的热情经久不衰，世界上许许多多的数学工作者和数学爱好者做梦都在想证明它呢。<br />
<br />
　　数学奇才伽罗华于１８１１年诞生在拿破仑帝国时代，经历了波旁王朝的复辟时期，又赶上路易・腓力浦朝代初期。他从小就显示了杰出的数学才华，中学尚未毕业他就写了一篇关于群论的论文，并提交给了法国科学院。审稿人是当时法国最杰出的数学家柯西，然而，柯西因为种种缘故将伽罗华的手稿弄丢了。１９２８年他中学毕业后，进入了师范大学学数学。第二年，他写了一篇重要文章，并将手稿交给了法国科学院常任秘书傅立叶。然而，傅立叶收到伽罗华手稿后不久就去世了，因而伽罗华的文章再次被遗失了。<br />
<br />
　　在师范大学，伽罗华参加了当时最激进的的秘密组织“人民之友”，并发誓：“我愿意用自己的生命唤起人民的觉醒。”他揭发了校长吉尼奥对法国七月革命政变的两面派行为，因而被开除了学籍。之后，他进一步积极参加政治活动。１８３１年５月ｌ０日，伽罗华以“企图暗杀国王”的罪名被捕。由于“人民之友”的解救，伽罗华被宣告无罪当场获释。同年７月，伽罗华组织参与了国庆示威，再次被抓进监狱。在监狱中，他度过了２０岁的生日。伽罗华一方面与官方进行不妥协的斗争，另一面还抓紧时间刻苦钻研数学。尽管牢房里条件很差，生活艰苦，他仍能静下心来在数学王国里思考。<br />
<br />
　　ｌ８３２年３月１６日，伽罗华再次获释了。不久，年轻气盛的伽罗华为了一个舞女，卷入了一场“爱情与荣誉”的决斗。伽罗华深知对手的枪法很好，自己难以摆脱死亡的命运，所以连夜写好了遗言：“我请求我的爱国同胞们、我的朋友们，不要指责我不是为我的国家而死。我是作为一个不名誉的风骚女人和她的两个受骗者的牺牲品而死的。我将在可耻的诽谤中结束我的生命。”<br />
<br />
　　接着，他把自己生平的数学研究写成论文手稿，他几次在纸边空白处写上“我没有时间，我没有时间。”他在黎明之前那最后几个小时写出的东西，足以让世世代代的数学家们忙上几百年。他不废吹灰之力就解开了那个折磨了数学家达几个世纪之久的谜：在什么条件下代数方程是可解的。他开拓了一个崭新的数学理论“伽罗华群论”，作为这个理论的推论，可以得出五次以上一般代数方程根式不可解，以及用圆规、直尺三等分任意角等结论。<br />
<br />
　　１８３２年５月３０日清晨，伽罗瓦在“决斗场”与他的对手相遇。伽罗瓦倒下了，肠子被射穿。伽罗瓦临死前，他的弟弟流着泪赶到了。伽罗瓦努力安慰他的弟弟：“不要哭，”他说：“我需要我的全部勇气在２０岁时死去。”他带着对数学的钟爱、对革命的憧憬、对女人的渴慕离开了人世。他研究数学前后还不到五年，却足以跻身于有史以来十位最伟大的数学家之列。<br />
<br />
　　在中国古代，数学属于旁门左道，所以研究数学的人很少。《九章算术》是中国古代为数学发展做出的最杰出贡献。《九章算术》是中国古代数学专著，承先秦数学发展的源流，进入汉朝后又经许多学者的删补才最后成书，这大约是公元一世纪的下半叶。它的出现，标志着中国古代数学体系的形成。<br />
<br />
　　《九章算术》共收有２４６个数学问题，分为九章，分别是：方田、栗米、衰分、少广、商功、均输、盈不足、方程、勾股。《九章算术》在算术方面的主要成就有分数运算和比例问题；在几何方面的成就包括面积和体积计算；在代数方面的成就主要有一次方程组解法、平方、立方、一般二次方程解法等。“方程”一章还在世界数学史上首次引入了负数及其加减法运算法则。<br />
<br />
　　中国古代对数学的另一贡献是关于圆周率的计算。三国时期，刘徽提出了计算圆周率的科学方法――“割圆术”，他用圆内接正多边形的周长来逼近圆周长。刘徽计算到圆内接９６边形，求得π＝３.１４，并指出：内接正多边形的边数越多，所求得的π值越精确。祖冲之（公元４２９－５００年）在刘徽的基础上，经过刻苦钻研，反复演算，求出π在３.１４１５９２６与３.１４１５９２７之间。<br />
<br />
　　中国古代对数学的贡献还有中国剩余定理。公元前后的《孙子算经》中有“物不知数”问题：“今有物不知其数，三三数之余二，五五数之余三，七七数之余二，问物几何？”答为“２３”。明代数学家程大位在其《算法统宗》里用口诀：<br />
<br />
　　三人同行七十稀，五树梅花廿一只。<br />
　　七子团圆月正半，除百零五便得知。<br />
<br />
　　这个口诀的意思是：把用３除所得的余数乘以７０，加上用５除所得的余数乘以２１，再加上用７除所得的余数乘以１５，结果若是比１０５大，就减去１０５的倍数，便得所求的数。<br />
<br />
　　然而，号称中国古代第五大发明的的科举考试从来就不考数学。科举考试从隋代开始建立，直到清末才废除，历经一千三百多年。没有数学，中国照样发展，唐朝的中国更是世界上最发达的国家。<br />
<br />
　　中国对数学的顶膜崇拜只是３０几年前的事。那时，中国刚结束十年动乱，刚恢复高考。中国发现自己在科技方面远远落后于西方国家，想迎头赶上，最佳突破口就是数学。数学不需要资金，不需要设备和仪器，更不需要实验室，只要一支笔、一张纸即可。那时，中国媒体大树宣传数学家华罗庚、陈景论、杨乐、张广厚、苏步青、谷超豪、潘承洞、王元等。徐迟的报告文学《哥德巴赫猜想》更是将数学推到了顶峰，一时间，陈景润在中国变得家喻户晓。大家奔走相告“陈景润啃着干馒头，证着一加一等于二，草稿纸都堆成了一座山。”陈景润这种刻苦钻研数学的精神激励了无数热血青年学数学。之后不久，从江西赣州又传来了数学神童宁泊打着手电躲在被窝里学数学的佳话。数学一下子变成了第一热门。<br />
<br />
　　中国国内现在仍然崇尚数学，从小学开始就有各种各样的数学培训班和数学竞赛。其中，难度最大的比赛要数“小学奥林匹克数学竞赛”，简称奥数。市面上各种各样的奥数复习书籍让家长直恨自己出生太晚了。有一次，记者拿着一道奥数题“问１、２、３、２、３、４、３、４、５、４、５、６……前５００个数的和是多少？”去问菲尔茨奖（数学界的诺贝尔奖）得主安德烈・奥昆科夫，竟然难倒了这位世界著名的数学家。这位数学大师说他从来没上过奥数，也不理解中国小学生拼命学奥数的做法。他认为那些太难、太刁钻的题目，很可能伤害了孩子们学习数学的兴趣。<br />
<br />
　　无独有偶，世界著名数学家、美国哈佛大学数学教授丘成桐也反对奥数。这位获得过菲尔滋奖的华人数学家这样评价奥数：“奥数题都是事先有答案的题目，只不过让学生钻更多的套去找到那个答案，这没多大意义。真正有价值的，是从没有答案的现实中发现问题，然后运用数学知识去解决问题。”丘教授在浙江主办了一个新型的数学竞赛，要求学生组成研究小组，自己到生活中去发现问题，然后用学过的数学知识来解决它。这种没有现成答案的竞赛方法，就与奥数竞赛完全不同，实际上是让学生学会提出问题和解决问题的科研方法。<br />
<br />
　　其实，美国的数学竞赛名目也很繁多，美国小学也有奥数，初中有Ｍａｔｈ　Ｃｏｕｎｔ，高中则有ＡＭＣ１０，ＡＭＣ１２，等等。美国的小学奥数有些题目也能难倒大数学家，比如这道题：“将２７个单位正方体胶在一起组成一个边长为３的大正方体，再从上往下、从左往右、从前往后各挖掉中间那个小正方体。如果你将这个被挖心的大正方体泡到一个漆桶，问：总共有多少表面积被油上了漆？”不过，美国很少办培训班，也鲜见复习书籍。美国一切顺其自然，一切依着小孩的兴趣，家长极少逼小孩参加数学比赛。美国人普遍不爱数学，不少有识之士都在为美国的数学危机而担忧。前联储主席格林斯潘曾经有一番惊人的论断：“美国时下的经济危机其根源是美国人的数学太差。”然而，当今世界上一流数学家大都是美国人。<br />
<br />
　　笔者以为，在美国小孩要学好数学需要天分、兴趣、勤奋三个要素。其实，这三个要素是相辅相成的。小孩有数学天分，就会对数学感兴趣；小孩对数学感兴趣，就会勤奋学数学；小孩越勤奋学数学，对数学的兴趣就会越浓，学起来也就会越容易。在美国，我们既不能逼小孩学数学，因为逼得太紧会使小孩厌烦数学；我们也不能放任小孩不学数学，因为小孩的天性都很懒。另外，我们不能让小孩越太多的级学数学，那样会让小孩对数学消化不良，时间长了容易学东忘西的。一般来说，越一至二级足够了，除非你小孩真是数学神童。再说，华人弟子有相当一部分将来要学医学和法律，花大量的时间学数学简直就是浪费。<br />
<br />
　　那么，在美国的中国家长究竟应该怎样对待数学呢？笔者以为，“三励法则”可助我们过关。我们应该像美国家长学习，多鼓励小孩。不管小孩做对了还是做错了数学题，我们都应该为他们喝彩叫好。毕竟，用来鼓励的话语都是免费的，小孩受到了鼓励就会对数学感兴趣、有信心。我们也应该多奖励小孩做数学。既然小孩不可避免地要玩电脑，何不将玩电脑作为一种对学数学的奖励办法呢？小孩玩了一会电脑，过了一把瘾，做起数学来也就更带劲。最后，我们应该多激励小孩学数学，告诉小孩勤奋比天分更重要。牛顿小时候并不是数学神童，但他很勤奋，发明了微积分。这样的例子还有很多很多。<br />
<br />
　　愿你的小孩数学学得更轻松、更快乐！</div>

							
							
							
							
