													<h2>乱弹易经―雷泽因何名归妹</h2>
						
						
																											<div id="postmessage_500659" class="t_msgfont">原创首发&nbsp;&nbsp;于2010-3-3<br />
<br />
占卜作为一种上古文明，根源于人们对神明、天道的敬畏之心。从一些史料来看，那时的人们遇有不明、不决的疑难事，首先是要问天（神明），而不像现在这样听凭所谓专家信口雌黄。秦汉之后，这种传统文明慢慢淡出人们日常的生活，个中原因难以理解。大概由于秦始皇烧书，汉武帝独尊儒术，杂博纷纭的易学着术（如连山易、归藏易）失传殆尽，而周易的唯一代表作《易经》也被《易传》给彻底代言了，以致后人把易经与易传混为一谈。易学的本质应该是卜筮、问天，是一门颇具实用价值的日常生活指导书，但由于易传对易经的释义、代言和发展，易学成了没有实用价值的道德说教之篇首了。我怀疑孔老爷子研究周易时下了很大很大的功夫，最终因年事已高未能窥径入门，解密失败，“韦编三绝”之后，只好云山雾罩地弄一通“看上去很美”地君子人生辨证哲学。从此，人们与天道神明沟通的渠道阻塞了，“子不语怪力乱神”，头顶三尺无神明，只剩得耳边之圣人谆谆。哦，还有那些流传千年，鹦鹉学舌，自相矛盾的易学文章在不知所云。<br />
<br />
易经六十四卦，每卦都有卦画、卦名。卦名有三个字的，也有四个字的，六十四卦卦名是由两部分组成，前两个字是卦画名，后面的一个字或两个字是卦意，比如 《风泽中孚》、《火泽睽》、《坤为地》、《山地剥》等。诚所谓“《干》刚《坤》柔，《比》乐《师》忧，《临》《观》之义，或与或求”。而这些卦名中最能生动体现易学奥秘，也颇有趣味性和人情味的卦名，当属第五十四卦“雷泽归妹”了。该卦为震上兑下，震为雷，兑为泽，所以卦画名雷泽，而卦意名为“归妹”。由于“归妹”一语比之其它卦名来说，似乎过于具体、形象、生动，惹得后世懵懂派易学人士产生无数联想、假设与遐思，各种牵强附会之说不免纷纷穿凿而来。<br />
<br />
有名牌大学之国学教授讲易经，对“归妹”是这样解释的：兑泽受震雷为撼荡，象征女子窝处于被动之地位。兑当“说”字讲，又有“悦”之意，今悦以动，女所必归从也。又震为长男，兑为少女，震兑相配，名为归妹。要按此逻辑解卦，《水泽节》卦上面是中男坎，以二哥对三妹，也完全可以叫“归妹”；而《泽雷随》卦也是兑与震（虽然与《雷泽》卦上下部位颠倒，但这种解卦逻辑并没有揭示上下的区别或奥秘），也没有理由不叫“归妹”啊？这样就违背了逻辑同一律，犯了双重标准或没有标准的错误，不能自圆其说。这种打哪儿指哪儿，顾头不顾尾的胡乱解释，充斥着周易研究领域的各个角落。我看了很多易学大师在解释卦辞爻辞时都是这个套路，令人不胜其烦。解释不通你可以放在那里嘛，易经不是豆腐，不会发霉变质，但是不要为解释而解释，败坏了国人的逻辑思维。<br />
<br />
我认为，探索易学奥秘，要从数理思路入手，六十四卦画其实就是六位二进制数的排序。从数值零（《坤为地》卦）开始升序排列，起至《山地剥》、《水地比》，最后八八六十四，大白天亮于《乾为天》。卦画中阳爻循此律从上而下或进步或回蓄，卦名、卦辞和爻辞都是围绕阳爻动态揭示出的兆应来说事、类比。那些望文生义的义理派引申、演绎，礼教铺陈完全可以无视。<br />
<br />
按照阳爻递进发展循序，《雷泽归妹》之前是《风泽中孚》，之后就是《火泽睽》，都是在兑卦一列中。《风泽中孚》是上巽下兑，按照人伦取意是（长女）大姐向三妹；《火泽睽》是上离下兑，是（次女）二姐向三妹；而本卦雷泽是（长男）大哥向三妹；一连三卦都是兄弟姊妹上下相对，这种巧合是不是应该取个好名字纪念它的意义呢？所以取名“归妹”甚为恰当。当然有人会问，为什么不说是小妹向着兄姊而是说兄姊们朝向小妹呢，道理其实很简单，兑卦小妹在下不动，而上面哥哥姐姐们次第而行，当然应该说归妹，而不是归兄、归姐了。或许有人会问，既然如此，为什么大姐向三妹之卦不称归妹，二姐向三妹之卦不称归妹，独独长兄向三妹之卦，就称归妹卦了呢？那么下面就要讲到易经的另一个奥秘，有一些卦名是在揭示卦变爻动的下一步态势，这也是窥探易经门径的最基本思路。《雷泽归妹》卦的结构是：[上部]上六、六五、九四，[下部]六三、九二、初九，阳爻依道从上而至渐次逼进，下一步必将变卦为[上部]上九、六五、九四，（下部不变），也就是从雷变为离，长男变次女，地道的长兄归二妹。所以，把《雷泽》卦命名为《雷泽归妹》，实在是意理所致，妙不可言啊！<br />
<br />
此文至此，我想说，用易占卜是人神交流，接受上天训导与启示，卦名之中必含天意。解卦要思朴识正才能心有灵犀，切不可拘泥于人云亦云之窠臼。比如说，假如有人与其妹妹走失，遍寻不见，若能占到此卦，我相信这必是找回其妹的吉示。</div>

							
							
							
							
