主题梗概：孔子是中国人数千年来的至圣先师，他的思想的核心归纳起来就是仁、礼、中庸 。所谓“仁”，就是爱人，“己所不欲，勿施于人”，孔子特别强调仁爱，这是他的贡献。所谓“礼”的核心就是宗法制，“君君臣臣父父子子”，各安其位，另外还可以用“乐”来调剂，乐有差别，多样统一，这就是和谐。所谓“中庸”，既是一种伦理学说，同时也代表了一种思想方法，追求中常之道，内外协调，保持平衡，不走极端，子曰：“过犹不及。”，这样一种思维方式对我们几千年的文明产生了深远的影响。<br />
<br />
孔子的思想在今天是否已经过时？<br />
<br />
立场1，孔子的思想没有过时，重新提倡孔子思想，在当今社会是必要的。现今社会急需重组君子阶层，形成仁爱、和谐的时尚风气。<br />
<br />
立场2，孔子的思想已经过时，重新提倡孔子思想，不符合当今社会的发展潮流。孔子的思想在历史上就具有极大的破坏性，是陈旧糟粕的东西。<br />
