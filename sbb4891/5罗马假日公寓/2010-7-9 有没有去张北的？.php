张北草原音乐节2010 攻了个略的您必须知道 <br />
<br />
不一样的张北，不一样的草原音乐节 <br />
张北音乐节不敢保证没人抱怨，但是，观众只要来，就一定能看到张北音乐节的进步。张北县城为了一洗去年干旱导致的耻辱，不仅草原保证有草，还准备了20万亩的花田草海。 <br />
<br />
道路：张北县城从去年音乐节一结束便开始了筹划，在硬件设施上进行改造。周边的道路全部进行硬化拓宽。土路已经没有了，虽然还来不及铺上水泥马路，但车辆驶过扬起千层土的情况完全改善。周边道路也加宽了，去年的四米道已经变成了七米道，尽可能地保障车辆的通畅。 <br />
<br />
交通：今年的公共交通全部市场化，票价根据市场价格制定，从北京到表演现场80元，从张北南站到表演现场20元，从张北县中心站点到表演现场10元。这样能够更好的保证数量以及班次，能够实现满员即走，完全不耽误观众看演出。 <br />
<br />
北京-张北始发车：7月29日17:00-19:00 ，张北-北京末班车：8月2日 11:00。 详细时刻表请登录音乐节官方网站：<a href="http://inmusicfestival.cn/2010/xinwen/2010/0622/176.html" target="_blank">http://inmusicfestival.cn/2010/xinwen/2010/0622/176.html</a> <br />
