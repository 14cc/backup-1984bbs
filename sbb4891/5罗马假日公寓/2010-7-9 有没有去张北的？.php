商户：今年共设有60个不同的摊位。吃、喝、玩都会有全面的供应。主办方在价格上会做统一的管理。摊位的选择采取投标的形式，保证摊位按照主办方的要求去进行销售，例如最贵的盒饭不能超过20块钱。将音乐节中整个消费控制在合理的大众化水平。摊位也有比较高端的商户，类似酒精饮品，啤酒、红酒都会有。今年的张北照顾各种消费的层次的人群，做到花三五块能吃饱，但想花三五百也能花得多元化。 <br />
<br />
张北草原音乐节音乐之特色娱乐区： <br />
<br />
创意市集：本届音乐节依然保留创意市集，但需要统一申请登记，包括摊主的个人信息，以及想要出售的东西，主办方会做一个基本的审核，并且向当地的公安备案。创意市集由主办方规划区域，统一进场，摊主将比观众先进入表演区。 <br />
<br />
放映区：放映是作为表演场地之外的一个主打区域。各舞台的演出完毕后，除了喝酒聊天去电子舞台跳舞之外，还可以到放映区去观赏露天电影。寻找小时候挤在一起看露天电影的美好回忆。 <br />
<br />
按摩区：提供给蹦了一天的观众最好的休息场所。有专也的按摩师在此恭候，给疲惫的身躯松松筋骨，迎战下一场Pogo大战。 <br />
<br />
