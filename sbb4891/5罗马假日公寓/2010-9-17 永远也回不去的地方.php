													<h2>永远也回不去的地方</h2>
						
						
																											<div id="postmessage_834507" class="t_msgfont">我的家乡是一个海边小城，始终也没怎么发展起来，生活带着一种平静舒缓的味道。整个夏季，因为剧烈的海风总是夜晚凉爽。骑着自行车上学一年四季。有一个不太景气的电影院和藏书不太丰富的图书馆。所有人都认识所有人。据说派出所抓到两个斗殴的人，都不用调解，让他们自己聊，半个小时后，必找出一个共同的亲戚朋友，一起挽手走出是也。<br />
<br />
 <br />
<br />
我想，所以人都和我一样，在离开了家乡之后，才发觉什么是自己的家乡。置身在一个城市里，你感觉不到一个城市的气质。就算你在一个人的身边，其实，你也许永远不了解那个人。一座城市，需要你生息相通，肌肤相贴地爱过、活过，才算熟悉；一座城市，需要千山万水、长途跋涉地远离它、隔绝它，才算真的看清楚它。当我置身于都市时，我渐渐读懂了我的小城市。<br />
<br />
 <br />
<br />
都市的大，对比出家乡的小，都市的繁华，对比出家乡的悠闲，都市的人情错综和淡漠，对比出家乡的人情浓厚和简单。总是一次一次的“远去”，让我的，一点一点贴近。但是我不愿意多讲这种情绪，这种所谓的“乡愁”。<br />
<br />
 <br />
<br />
总是一次次的远去，让我的心一点点的贴近。但是我不愿意多讲这种情绪，这种所谓的&quot;乡愁&quot;。<br />
<br />
 <br />
<br />
乡愁是怎么酿成的？我想，这已经不是这个时代的命题。那是上一个时代，工业社会刚刚兴起，无数人背离家园，传统的农业生活方式和生活关被冲击，古典的四世同堂在高昂的房价之下成为梦想，田园成为我们整个人类怀念留恋的一种情怀。<br />
<br />
 <br />
<br />
因为，没有人逼你去大城市，没有人逼你做出逆流而上的大马哈鱼，你之所以留在一个压力巨大生存沉重无比奋力也几乎泯然众生的城市，因为你愿意，因为你接受了这个城市的规则。这些规则严酷得近乎伦理，因为在一年之中，总有一两个时刻，当你走出车站，怀揣着梦想，你觉得这是一个希望之城。因为你青春正盛，你觉得你很大，家乡已经容不下你，必须到一个大的世界硬碰硬，因为你不要安稳，要兴奋。因为你不要舒服，要剧烈。因为你要一个万花筒一样，永远神奇莫测，每一次都不一样的世界。<br />
<br />
 <br />
<br />
故乡，其实是一个永远也回不去的地方。因为，你的心已经变了，每次回到家，初三天，紧绷的神经仍旧紧绷，要一周左右，才完全散淡下来，彻底放空自己。十天半个月，你觉得舒服无比，而半个月之后，你开始觉得无聊。是的，不好意思，父母相伴，但是你仍旧觉得无聊，你开始想念大城市的烟尘车声人际纷争，那起码是一个鲜活的世界。不要不承认，就像食肉动物，无法再食草一样。每一个出走的人，都以为自己随时可以回家，而这正是人生的悲剧。<br />
<br />
 <br />
<br />
其实，家，在你转身离开的那个瞬间，已经永远无法抵达。而更大的悲剧，我想，在于，我们的父母无法进入我们现今构造的世界，这座盛大的城市，对他们来说，不如门口有条杨柳拂面的小河。以及我，我们的孩子，那逆流而上的大马哈鱼的后代，他们更加无从想象，那么遥远的小城市――哪怕在我们怀念的口吻里，变得越来越美好不真的伊甸园。<br />
<br />
 <br />
<br />
我们遥望着下游日益孱弱的父母，我们奋力挽着同在上游的后代。是的，我们可以告诉自己，如果没有我们前赴后继的滋养，不会有这一座巨大的都市。大马哈鱼为什么而游？我想，是因为永远也无法实现的渴望。<br />
<br />
 <br />
<br />
后记：风筝总是渴望天空，但是天空么有尽头；于是飞呀、飞呀，那根牵挂着风筝的线越来越脆弱，风筝却毫无觉察；终于有一天线断了，于是我们再也找不到归来的方向！！</div>

							
							
							
							
