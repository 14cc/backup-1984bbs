1.型号问题：很多人分不清楚内衣尺码所代表的意义，误以为数字越大则胸越大，导致很多大胸的MM认为下围大的内衣才适合自己。其实数字代表的是胸部下围的位置，选择尺码过大则会导致内衣上移，尺码过小则导致有勒紧感，呼吸不畅胃部不舒服。而后面的字母及ABCD代表了你胸部的大小，应该按照上胸围与下围之差选择，你的胸部越大则应选择的字母越大。<br />
<br />
2。不要为了面子而选择不适合自己的内衣尺码。罩杯（ABCD）如果选择过大，及内衣上沿经常中空，其实看起来很不美观（不要以为别人看不出来其实蛮尴尬），而且这样的穿着只会让胸部脂肪越来越少。而有些女生因对大胸的讨厌情节，穿着比自己尺码小的内衣，导致上沿有压痕，则可能会让胸部脂肪出现分块现象。穿着过厚的文胸去挤压乳沟也会导致胸部脂肪的流失。<br />
<br />
3。不要为了漂亮而选择一些日系的可爱或者性感的内衣，通常这类内衣的固定功能都很差。而一些不够好看的调整型内衣反而会对你的身体更好。<br />
<br />
4。内衣的内里最好选择纯棉质地，过硬的内里会导致乳晕颜色的加深。乳晕过大的女生，请节制和不同男人的X生活。当然胸很大乳晕也会大。<br />
<br />
5。关于健康丰胸，其实胸部大小大多是遗传，使我们与生俱来的，不要太过纠结。想要丰胸的女生可以多吃一些胶原蛋白的丰富的食品，但是其实想丰胸总是要先长胖的。还可以稍微按摩一下。想要减胸的女生，就节食好了，最不健康的减肥方式，当然是先小胸了。<br />
<br />
6。注意内衣的调整，每次去卫生间最好的调整一下内衣。防止外扩和产生副乳，且能固定脂肪。</div>

							
							
							
							
