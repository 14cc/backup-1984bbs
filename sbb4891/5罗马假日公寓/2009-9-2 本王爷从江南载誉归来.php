<br />
一、从北京飞到上海，赶上那里的阴雨天，听当地人说，这阵子每天下午三点左右，准时来一场雨。我在上海逗留了两天，果然灵验。那天气潮得让我裆部发痒，菊花盛开。<br />
<br />
食、住、行，全可归结一个字：真他妈贵！<br />
<br />
由于黄浦江里有怪物，当地政府将整个江路两岸封锁，筑起高高的牌子防范异形上岸，连东方明珠周围也充斥着工事设施，所以我携老婆一行人等，仅在海洋馆边上的味千拉面吃了在上海唯一一顿还算可口的午饭，就匆匆滚回到外滩所住的创业酒店。<br />
<br />
向西最远到达上海的步行街，本想在最繁华的街区里，免费参观一下传说中上海小女人的美貌，却再次败兴而归。<br />
<br />
魔都啊，魔都！你是我此生来过一次，就不想再来第二次的城市。两个夜晚后，我挥一挥衣袖，不带走一个二奶。<br />
<br />
<br />
二、就好比从地狱到了天堂，只听“咻”的一声，我就穿越云层来到了厦门。走出机场大厅，闻到海风，我就已经知道，我这后半辈子，很可能会美滋滋地将一把老骨头埋在这美丽的小岛上鸟~~<br />
<br />
厦门这一节，好事忒多，三言两语难以表述，暂且扣下不说。嘿嘿~~<br />
<br />
<br />
三、从厦门出发北上，来到杭州，本来应该是我华东之旅的第一站，谁想到，天妒英才，在杭州的第一天夜里，我竟然染上了急性肠胃炎!<br />
<br />
一颗硕大的巨星，眼见即将陨落，五百年才出世一次的智慧与勇武、美貌与才华完美相结合的王爷，就这样在巨大的幸福与喜悦中，骤然病倒。全世界都沉浸在悲伤的气氛中。<br />
<br />
“呜呼，九王爷!你不能就这样离偶们而去!你还承担着改良人类基因的历史重担啊!”昏睡中的我，耳畔仿佛听到了若干美少女们发自灵魂深处的祈祷。我，不能就这样走了!<br />
<br />
于是，在我的小宇宙爆发那一刹那，我的双目睁开，我的菊花洞开，我的@$^$%流了出来。。。<br />
<br />
杭州，今次我与你无缘，未能在西子湖边静静的欣赏杭州美女们穿行的倩影，但是，我不甘心，我一定要再来一次，马力十足的在西湖边跑上十几圈，方解心头憾意。<br />
<br />
杭州，I will be back!<br />
<br />
<br />
四、原打算从杭州做为起点，一路上在苏州、吴锡、南京等地处处留精，只因突发的病恙作罢。从8月22日至9月2日，这十二天，即是我首次蜜月之旅。将来，我会在经过编辑加工后，以神话的形式讲给我的小宝宝们听，并附上他们爹娘的照片以做参考。<br />
<br />
<br />
十多天后头一次上网敲字，瘾过完了，洗澡切~~<br />
