													<h2>也说说石化子弟</h2>
						
						
																											<div id="postmessage_781990" class="t_msgfont">此为转帖。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp; &nbsp; 这是个让人很难理解的企业，大到一般人无法想象。除了银行，几乎所有的社会服务部门都是自己的。在我上学的时候，企业拥有九所小学，六所初中，两所高中。拥有四路公共汽车在厂区驰骋。真正的公交车，从头做到尾差不多要一个多小时的时间。有自己的企业医院，服务公司，商业公司，甚至醋和酱油都是自己的三产企业制造的。大概有二十多万职工和家属，活脱脱的一个县城，但又和县城有很大的区别。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;大家来自四面八方，所以都说一种语言，普通话，这一点和部队大院有些相似。 <br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;所有的人基本都是企业的职工或者家属，基本没有外来或者流动人口。所以所有人的收入相似，家庭条件相似，不存在一般地方上的穷学生富学生之类的问题，对谁家有钱没钱也是个很淡漠的概念。彼此之间也非常的信任，毕竟都是一个公司的，家长间多半都熟识，低头不见抬头见，什么事情惹出来，找来找去也就能找到熟人，也就协调下去了。 <br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;石化的生活水平还算不错，在八十年代到九十年代初期是远远强于地方的，只是现在有些落后。那时候液化气30公斤瓶的是六毛钱，送到家里是一块八，送上门的钱是一块二，这是一般地方很难想象的价格。这个价格一直维持到九十年代中期。单位逢年过节都有大批的东西分配，米面油鱼肉水果基本都不用自己买，这一点在那个年代可以说是地方上垂涎欲滴的。不过随着时间的推移和一些舆论的关注以及石油行业的变化，现在的福利越来越少拉。 <br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;石化主要分为厂区和生活区，所有的生活区住宅同一批都是一样的，只是有新旧的区别，所以也没人去比较什么房子的大小，这是和地方另一个不同的地方。房子基本都是分的，后来改制，大家都出了点钱，就买下来了。 基本无所谓大小或者新旧，因为都差不多的。&nbsp; &nbsp;<br />
&nbsp; &nbsp;&nbsp; &nbsp; 石化是个很封闭的地方，这一点经常被诟病。不过封闭有封闭的好处，出来看看，还是石化的孩子更单纯，更容易相处。毕竟在石化，你要是学坏或者做了坏事出了名，很快就会全公司皆知，家长的面子就特别的过不去，过不去肯定就要管制，所以小坏蛋有，大坏蛋就没有了。虽然有些痞气的，但心地都很善良。和地方上的一些匪类，有很大不同。石化的痞子都没有穷凶极恶的，见到父母的朋友，也都要毕恭毕敬的叔叔阿姨。这一点其实很可贵。 <br />
&nbsp; &nbsp;&nbsp; &nbsp; 因为都在自己的学校上学，所以有另外一件地方上的孩子很难理解的地方，竟然我们会有很多从小学一直到高中的同学，甚至有人在幼儿园就是一个班的，一直十几年下来到高考，甚至高考还考到一个学校。所以石化同学的感情和其他地方上大不相同，到了外地也是一样。大部分石化人很少说自己是淄博人或者临淄人，大多说自己是齐鲁石化的。而在很多地方，石化的孩子和淄博的孩子也是格格不入的，完全两种思维模式。 <br />
&nbsp; &nbsp;&nbsp; &nbsp; 小时候石化效益不错收入也不错，孩子都没有吃过苦，也没收到过什么生活的坚信，实在不行，大不了去技校然后留在石化做工人。大部分的石化孩子都不愿意回石化，这和石油大学的毕业生最高梦想就是来石化成了鲜明对比。那差不多算是最后的选择之一。当然，随着经济的恶化，现在回到石化反而成了一种更好的选择，也许现在孩子的世界观会有一些改变。 <br />
&nbsp; &nbsp;&nbsp; &nbsp; 石化有自己的招生办，行政上和淄博市是平级的，也就是石化有权自己划分数线，所以石化的分数线往往和油田并列山东的倒数一二名，也就是说，那时候石化的孩子高考的竞争对手就是自己。后来招生办裁撤了，划到了和淄博市一样。我们这一代算是这个福利政策受惠的最后几届了。 <br />
&nbsp; &nbsp;&nbsp; &nbsp; 临淄区的经济差不多就是石化这十来万人支撑的，没有了石化，临淄区也就差不多了。并非妄自菲薄，哪个城市或者县区有这么巨大且稳定的中高收入群体。一个一百万的城市，都未必能有二十万高收入群体，而石化至少有十几万。这还不包括企业的采购。换句话说，你就是给石化提供灯泡啥的，都足够你发财致富的了。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;石化的孩子善良而且骄傲，学习不太好，就我行走江湖多年的经验来看，全国应该属山东人最不错，山东人里面应该是石化最不错，也许是脾气相投的原因。不鲁莽、不精明、不算计、不占人便宜、不强出头、不拉帮结伙、不哥们义气，挺大方分的也清楚，做事也比较公道，基本到了各个高校，也算是比较核心和优秀的群体。主要原因大概就是共产主义似的的生活环境，和较低的分数线把。最主要的是没有那些小市民习气，很平和中庸而又有魅力。 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 石化的工人不少都是一线，石化的孩子虽然谈不上吃苦耐劳但并不娇气，这和地方上生活条件好的家庭总是小太阳有所不同。也许因为水土问题，石化的孩子尤其是女孩子的身材我感觉比地方上差的比较远，远没有地方上的玲珑有致。但长相又强的太多，多半是生活条件好带来的细皮嫩肉。 <br />
<br />
