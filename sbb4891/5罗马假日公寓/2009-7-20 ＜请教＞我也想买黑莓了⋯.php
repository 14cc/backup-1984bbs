5320&nbsp; &nbsp; 108×46×15mm&nbsp;&nbsp;<br />
8300&nbsp; &nbsp; 114×66×14mm<br />
大约宽一点，长一点。还是看看真机才有感觉。<br />
<br />
另外黑莓也用st键盘的81系列机型，相对来说要小些，但是总觉得非全键盘少了黑莓的特色。<br />
<br />
2、水深问题。不知道是不是黑莓特色，要搞清楚硬解、打孔、飞线、换壳、翻新、14天、0通话、反键这些概念，也是造成同样的机子价格差了一倍的原因。个人觉得只要不是打孔飞线，换壳其实无所谓的。我用得最多的7290和8800都不是原装，摔了很多次，进过水也没什么问题。7290前段时间100块卖掉了。<br />
<br />
3、买机问题。最好还是就近取材，面交最好，可以用工程模式自行测试，能通过一般就没事了。当面看呢，很多问题就可以马上知道，象外壳啊，屏幕啊这些，换起来麻烦。当然不差钱可以找最牛的几个卖家，价格贵很多，不过省心一些。北京中关村有很多卖黑莓的，不过在maxpda的投诉区也有很多问题，印象中论坛里有个天津卖家价格很好，反馈也不错。<br />
<br />
还有个办法就是在论坛里找玩家出售的，可能品质更可靠，毕竟自己买的时候都是精挑细选过的。不过更花精力，也是良莠不齐的。<br />
<br />
<br />
用了黑莓最大的坏处就是不想换其他手机了。</div>

							
							
							
							
