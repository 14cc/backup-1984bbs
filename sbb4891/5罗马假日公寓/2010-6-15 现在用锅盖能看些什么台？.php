																											<div id="postmessage_687659" class="t_msgfont">这个东西我略微了解。<br />
首先，锅盖这个叫法就不对，因为接收信号的是天线中间的高频头。一个高频头只能接收一颗卫星的信号，要想同时接收到多个卫星的信号不光需要多装高频头，而且还要一个切换开关。更重要的是，卫星的位置不同，所以一般一个天线的朝向只能接收到一颗卫星。<br />
<br />
对准了卫星，就可以接收电视了。但问题是你想看的节目不一定都在一颗卫星上，没准你想看的那几个分别在好几颗星上呢。而且有的还加密，你用免费的接收机只能接收不加密的频道，像是discovery这种是肯定加密的。当然，有些转播商，把好的节目弄到一颗卫星上然后加密卖卡收钱，不过这个费用就比较高了。<br />
<br />
接收机，其实就是和机顶盒一样打，功能是把接收到的信号解调然后传给电视。免费接收机很容易理解，所谓卡机就是可以看收费节目的接收机（仅仅从功能上来说）。机器后面有一个可以插IC卡的槽，这个卡就很关键了。你在家看数字电视是当地电视台给你办的卡，你要看卫星电视的加密节目就要去找响应的转播商买卡。<br />
<br />
接收机的品牌很混乱，其实大多都是国外作出一个机器，然后国内开始买芯片山寨没什么说的好像比较流行的现在是dream500和800 。还有早期的雷霆什么的。牌子很杂，这个要去专门的论坛学习。即使是dream机器也大都是国内山寨的，真正原装的很少。<br />
<br />
盗版，找转播商买卡那价格不是个人用户可以接受的，而且像是HBO这种都是天价。所以有了盗版，早期的盗版是卖盗版卡，当然这样做很容易就被封了。现在先进了，例如上面说的dream机器，除了卡槽之外还有个网线接口，插到路由器上以后，直接连接到专门的盗版商的服务器就可以看正版电视节目了。所谓盗版商就是他去买一个正版卡，然后把正版卡的信息通过网络发送给你的机器，这样你要你上网，每个一段时间就会接收一次验证信息，这个信息正确了，你的接收机才会正常解码，不然接收机不能解码加密节目。<br />
<br />
差不多就是这样了。</div>

							
							
							
							
