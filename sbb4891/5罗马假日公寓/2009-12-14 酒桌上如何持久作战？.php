													<h2>一点小建议</h2>
						
						
																											<div id="postmessage_372856" class="t_msgfont">兄弟，借我哥们当年教育我的话和我的经验：<br />
1、要练喝酒，就直接练高度白酒，其他什么啤酒、红酒、低度白酒都没用。高度白酒能喝一斤不倒的时候，其它酒就是水……<br />
2、喝酒之后千万别喝茶，喝茶稀释酒精没用，这点早已被证明了。而且醉酒之后喝茶只能让你更快的吐，酒茶同饮还伤肾。<br />
3、喝啤酒别用大杯，其实拼啤酒不是比喝酒，是比谁胃大，比谁排泄的快。所以要尽量用小杯子。别人不敬你就不喝，自己别没事上去敬酒。<br />
4、能不吐尽量别吐。吐不仅伤胃，而且吐了之后就上头了。往往吐前只是晕，吐了就不醒人事。再者，喝啤酒，喝到想吐最后忍住没吐才能增加酒量（其实是增加胃的适应性），喝完就吐，你喝一百年酒量也上不去。想吐的时候要尽量深呼吸，吃点冰凉的东西。千万别躺，一躺下准吐，最好是能站起来走走。<br />
5、喝酒前可以喝一杯酸奶，会在胃粘膜上形成一层保护膜，保护胃。<br />
6、给自己定一个量，到量了直接扣杯，说什么也不喝了。当众扣杯总比当众出丑强。（当然，有些时候不敢这样，比如陪上司）<br />
7、要有策略。比如，提前三杯就要开始打预防针。你能喝10杯，11杯就很可能吐，那就在第8杯的时候就跟朋友说“最后一杯了！酒量不行呀”，然后装欲吐状（用手捂嘴什么的），这样其他喝酒的人必然不敢象之前那样猛灌，至少会放慢敬你的速度和频率，给自己赢得一些喘息的时间。别人再劝酒，你就推托一阵然后“艰难”地喝掉，当然继续做欲吐状和酒醉装，这个需要演戏的本事，哈哈。记得，能喝10杯就最多喝9杯，因为最后肯定有一次所有人共同举杯干掉，这杯酒不能推，所以要留出量来，别临完事反而挂了，那就前功尽弃了。<br />
8、别在酒桌上逞能，酒量再大也不代表你本事过人，时刻记着酒桌是人生无数战场中最小的一个，不值得拼命。另外，要保持清醒，喝得越多越要告诫自己少说话（这个比较困难）。<br />
9、如果你真的过敏，还是尽量别喝了，有危险的。</div>

							
							
							
							
