■AV大久保 <br />
1998年香港。鬼才导演葛民辉把厚积了一系列的实验，悬念以及失败产物整合《初缠恋后的二人世界》出版。其中一部《AV大久保》讲述了一个名为大久保的日本黑社会，前往香港寻找昔日恋人的故事。当片中这个一直等待爱人的男人奔跑在九龙夜幕下，“AV大久保”几个字跃然屏幕――武汉乐队“AV大久保”的名字便来自于此：浪漫、有趣、怪。AV大久保乐队的武汉B级摇滚让我们体验到更多的是幼年时弥散在周围却或许并不明白的娱乐气氛。这种娱乐不像当年迪斯科舞厅和小剧场里的单纯聒噪，它被时代所连带出客观的视角和嘲弄的口吻。 <br />
<br />
AV大久保乐队在2006年组于武汉，成员包括：主唱/键盘：陆炎；吉他：谭超；贝司：左翼；鼓手：胡鹃。06年9月乐队在武汉先后两次为SMZB演出暖场，特别是改编自SMZB的“Where's My Vote”的雷鬼版本得到了台下观众热烈反响，效果极佳。从2007年起，乐队排练频率增大，演出次数也增加，先后担了：Orange（乌拉圭英伦乐队），The Last Chance of Youth（北京独立硬核乐队），The 4 Sivits （德国硬核乐队），Ratata（纽约电音摇滚乐队），Battles（纽约摇滚乐队）的特邀演出嘉宾。在2008年的摩登天空音乐节，AV大久保更是表现不俗，掀起全场火爆气氛。2008年10月，乐队与金牌制作人Martin Atkins在亚洲最大的录音棚A-String录制了他们的首张专辑《大时代》。 <br />
