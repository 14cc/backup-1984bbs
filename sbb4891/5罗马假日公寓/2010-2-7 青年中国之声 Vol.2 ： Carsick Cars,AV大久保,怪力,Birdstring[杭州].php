■Carsick Cars <br />
Carsick Cars由主唱和吉他手守望（24岁），鼓手李青（26岁），以及贝司手李维思（25岁）组成。乐队成立于2005年的3月，当时还是北京理工大学学生的守望在一个朋友的排练室里遇见了李维思和李青。5月时他们在一次小型的朋友聚会上做了他们的第一次演出，在那一年的年底，他们开始在北京的俱乐部里表演。他们第一张专辑录制于2007年的春天，由 P.K.14乐队的主唱杨海崧制作。 <br />
<br />
他们的声音，受到了Velvet Underground，Suicide和Sonic Youth的影响，同时也拥有Steve Reich和Glenn Branca的质地和结构，混合着粗糙的咆哮和强硬的开放和弦，这使得他们优美的歌曲呈现出一种探索性以及破碎感。在爆炸性的噪音和回授中，这些歌曲在某些瞬间透露出它们是多么地优秀，而这在许多北京的音乐家的身上也有所反映，许多音乐家也已经成为他们的歌迷――Carsick Cars的歌曲也许是中国地下乐队中被其他乐队翻唱最频繁的。 <br />
<br />
作为受到全世界瞩目的中国地下乐队中的一支，Carsick Cars已经在中国和国外做了大量的演出。他们去过中国的各大城市的俱乐部，并在 2007年的8月和9月，Sonic Youth――他们将Carsick Cars称作他们最喜爱的中国乐队――邀请他们一起进行欧洲巡演。在他们伦敦的演出之后，Carsick Cars受到邀请参加2007年10月奥地利的Steirischer Herbst音乐节，以及2008年五月伦敦的All Tomorrow’s Parties音乐节，并成为Dinosaur Jr.专场表演嘉宾。随着海外歌迷群的不断聚集，乐队已经跻身世界优秀乐队的行列，2009年5月被邀请参加欧洲音乐盛会之一的巴塞罗那Primavera Sound‘09音乐节，9月与Joyside横跨欧罗巴巡演并在之后与P.K.14，小河进行了首次美国巡演，所到之处无不令美国乐迷疯狂。回到中国后，乐队应邀参加了星空卫视海外部强档专栏Asian Uncut的圣诞特别节目录制，并以音乐嘉宾的身份在节目的最后环节表演了《Dear Friend》和《中南海》两首歌曲。而在2010年2月，乐队将首次踏上韩国的土地，与Maybe Noise旗下的10进行小规模巡演。 <br />
<br />
伴随着乐队一起，Carsick Cars乐队的成员们在许多其他的音乐计划中也非常的积极。守望是实验组合White乐队的创始成员之一，他们的第一张专辑由Einsturzende Neubaten乐队的领导者Blixa Bargeld2007年5月在柏林制作完成。2006年的2月他作为乐手参与录制了Glenn Branca的《Symphony 13》，并且与作曲家Elliott Sharp和Alvin Curran一起多次合作演出。2007年7月在纽约的一个音乐节上的表演后，他被邀请参加他们的第二张专辑的录音。李青和李维思是三人乐队Snapline的创始成员，他们的第一张专辑由PiL的鼓手Martin Atkins制作，并将在他的Invisible Records发行。此外李青也参与过Elliott Sharp的演出。 <br />
<br />
乐队已经被中国，英国，德国，瑞典，法国以及美国等国家的媒体广泛地报道过，并且出现在美国，德国，澳大利亚和新西兰的电视节目中。2007年9月他们被《That’s Beijing》列为中国最优秀的乐队之一，而 2008年9月，《音乐时空》中一篇文章将守望列为中国10位青年杰出吉他手之一。2009年底，新专辑《You Can Listen,You Can Talk》被华盛顿邮报评为本年十大专辑之一。 <br />
