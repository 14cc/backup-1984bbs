Carsick Cars is a Beijing-based band formed in March 2005 by vocal/guitarist Shouwang (aka Jeff), drummer Li Qing, and bassist Li Weisi, when the three were university students at the Beijing Institute of Technology. Shouwang met the other two members at a friend’s rehearsal space, hit it off very quickly when they found that they loved and were influenced by the same music, and in May performed their debut show at a friend’s party. Almost immediately,knowledgeable people began talking about the band, and although in the beginning they had trouble getting shows at the main clubs in Beijing because of their abrasive guitar sounds and the low-key intensity of their performances they began a series of performance in smaller clubs and rehearsal spaces around the city. In 2006 they teamed together with three other young bands equally disillusioned with the Beijing music scene to form No Beijing. This was the catalyst that caused them suddenly to receive a great deal of attention and they found themselves playing larger clubs and even touring around the country. <br />
<br />
Their debut album was recorded in spring of 2007, produced by P.K.14 vocalist Yang Haisong, who has remained a friend, adviser, inspiration and sometimes sound man to the band ever since. Their second album was produced in October 2008 by legendary New York producer Wharton Tiers, whose credits include Sonic Youth, Dinosaur Jr., Swans, and Helmet, among many others. <br />
<br />
The band is highly influenced by The Velvet Underground, Suicide and Sonic Youth, was well as by the repetitive structures and harmonic developments of Steve Reich and Glenn Branca. Like many of China’s best musicians they create elegantly-constructed melodies and are fascinated with the textures of different sounds and ways of combing them. Their music mixes rough abrasive shrieks and harsh,even outrageous, chords with delicate guitar lines that have an almost shimmering lightness. The exploding noise and feedback that weaves through their music inspired one of the Beijing underground’s earliest graffiti tags “Jeff loves noise.” Almost from the start Carsick Cars has received enthusiastic responses from fellow local artists and they are especially loved by Beijing’s growing hordes of sophisticated young musicians, making them the Chinese underground band whose songs are most frequently covered by other bands. <br />
<br />
As their reputation spread, they began touring actively around the world, in China with the likes of These Are Powers, Ex-Models, and Yann Tiersen. In the summer of 2007 they were asked by Sonic Youth to open for them during their European tour, and have since maintained a close relationship with the band. At the London end of their tour the band was invited to Steirischer Herbst Festival in Austria. The following May they played London’s All Tomorrow’s Parties Festival,opening for Dinosaur Jr. After several more European shows the band returned to China for a 20-city tour with label mates Gar before returning to Europe for a series of shows in England, Switzerland and Spain. The tour culminated in a 7 p.m. slot at Barcelona’s Primavera Sound Festival, one Europe’s largest festivals. The band is currently preparing an October tour in Europe with label mates Joyside before joining five other Maybe Mars acts for a series of widely anticipated shows in New York (four separate performances) followed by an East Coast tour of the United States.Returning to the country, the band went on filming the Christmas Special session for Asian Uncut as guest musicians on this hit program of Star TV International. In Feburary 2010, Carsick Cars will make their debut appearance in Korea with another Maybe Mars lable mates 10, on a small-scale tour. <br />
<br />
Beside Carsick Cars, the three members are also heavily involved in other music projects. Shouwang, for one, is also one of two members of“White”, one of China’s most admired experimental bands, and whose first album was produced in May 2007, by Einsturzende Neubaten frontman Blixa Bargeld in Berlin. The year before, in February, he also participated in Glenn Branca’s “Symphony 13”, and has since then collaborated several times with composers Elliott Sharp, Alvin Curran,Christian Marclay and John Myers. His chamber orchestra piece“Xizhimen Traffic Lights” was performed by the Beijing New Music ensemble and broadcast by the BBC in 2008. Li Qing and Li Weisi found“Snapline”, as guitarist and bassist respectively, along with vocalist Chen Xi. Snapline’s debut album was produced by Martin Atkins,drummer of PiL and Killing Joke, and the album was released on his own label, Invisible Records. Li Qing, who has also collaborated with Elliott Sharp, formed a second band with Li Weisi in 2009, called Soviet Pop. <br />
