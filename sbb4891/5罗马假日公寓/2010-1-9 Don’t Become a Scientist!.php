师典型的收入是科学家的2-3倍（这还得是那些运气忒好的正教授科学家们）。我也做<br />
了这个选择。我成为一个科学家是为了有自由来解决那些让我感兴趣的问题。但你可能<br />
并不能得到这种自由。作为一个博士后，你只能按照别人的想法来工作，可能被当成一<br />
个技术员来使唤，而不是作为一个单独的科学家来合作。最终，你可能被彻底排挤出科<br />
学界。你可以得到一份很好的工作，比如计算机程序员，但为什么不在你22岁的时候做<br />
这份好工作，而要在科学界的人才市场上面悲悲惨惨地混上10年先？你再科学上面花的<br />
时间越多，你会发现你越难离开，而且你对其他行当的雇主而言变得越来越没有吸引力。<br />
也许你脑瓜足够灵光，以至于你能跳出博士后的陷阱。有些大学会被你打动而在你博士<br />
毕业2年后给你一个可能的永久职位。这是可能的。但是科学劳动力市场的整体掉价意<br />
味着最灵光的脑瓜也得被拴在博士后磨盘上当驴转上很长时间。想想上面举过的例子吧<br />
。许多看上去非常有才而且有傲人的成绩和推荐信的人，后来发现研究上的竞争比其他<br />
一切的奋斗都要困难。<br />
Suppose you do eventually obtain a permanent job, perhaps a tenured<br />
professorship. The struggle for a job is now replaced by a struggle for<br />
grant support, and again there is a glut of scientists. Now you spend your<br />
time writing proposals rather than doing research. Worse, because your<br />
proposals are judged by your competitors you cannot follow your curiosity,<br />
but must spend your effort and talents on anticipating and deflecting<br />
criticism rather than on solving the important scientific problems. They’re<br />
not the same thing: you cannot put your past successes in a proposal,<br />
because they are finished work, and your new ideas, however original and<br />
clever, are still unproven. It is proverbial that original ideas are the<br />
kiss of death for a proposal; because they have not yet been proved to work<br />
(after all, that is what you are proposing to do) they can be, and will be,<br />
rated poorly. Having achieved the promised land, you find that it is not<br />
what you wanted after all.<br />
What can be done? The first thing for any young person (which means anyone<br />
who does not have a permanent job in science) to do is to pursue another<br />
career. This will spare you the misery of disappointed expectations. Young<br />
Americans have generally woken up to the bad prospects and absence of a<br />
reasonable middle class career path in science and are deserting it. If you<br />
haven’t yet, then join them. Leave graduate school to people from India and<br />
China, for whom the prospects at home are even worse. I have known more<br />
people whose lives have been ruined by getting a Ph.D. in physics than by<br />
drugs.<br />
假设你最终拿到了一个永久教职，一个终身教授职位。现在你不必为每两年一次的工作<br />
而奋斗，取而代之的是为研究经费而斗争。你会又一次地发现，这个世界上的科学家有<br />
一大箩筐，而你自己又不值钱了。现在你焚膏继晷地写研究计划，而不是去做研究。更<br />
糟糕的是，因为你的研究计划会被你的同行竞争者来审阅，你就不能按照你自己所好奇<br />
的东西来写。你得把你的努力和聪明才智浪费在怎么咬文嚼字地让那帮混蛋不要挑刺上<br />
，而不是去解决重要的科学问题。这是两个截然不同的事情：你不能把你过去的成功写<br />
进研究计划，因为那些是已经完成的工作；而那些原创性的天才想法还没有被证明。一<br />
句谚语说，原创性的想法是研究计划中的死神之吻（乍看有益但实则会导致毁灭的行为<br />
），因为这些想法根本就没有被证明可行（废话，被证明可行了你还写个屁的研究计划<br />
），因此它们会被认为是垃圾。因此，当你费劲千辛万苦终于到达了那“应许之地”的<br />
教授职位上，你会发现这根本就不是你原来想要的。<br />
那么，你能做什么？对任何年轻人（即任何还没有取得科学界的永久职位的人）来说，<br />
首要任务是去找一份其他的工作，这讲是你避免失望的痛苦。美国年轻的一代已经觉醒<br />
，看到了科学界黯淡的发展前景以及无法拥有一个合理的中产阶级生活，因此他们已经<br />
不愿意做科学家。如果你还没有觉醒的话，赶紧加入他们的行列。把博士班留给印度人<br />
和中国人吧―― 他们的家乡情况更糟。在我所认识的人中，人生被读物理博士所毁的<br />
人数比被毒品所毁的人还要多。<br />
If you are in a position of leadership in science then you should try to<br />
persuade the funding agencies to train fewer Ph.D.s. The glut of scientists<br />
is entirely the consequence of funding policies (almost all graduate<br />
education is paid for by federal grants). The funding agencies are bemoaning<br />
the scarcity of young people interested in science when they themselves<br />
caused this scarcity by destroying science as a career. They could reverse<br />
this situation by matching the number trained to the demand, but they refuse<br />
to do so, or even to discuss the problem seriously (for many years the NSF<br />
propagated a dishonest prediction of a coming shortage of scientists, and<br />
most funding agencies still act as if this were true). The result is that<br />
the best young people, who should go into science, sensibly refuse to do so,<br />
and the graduate schools are filled with weak American students and with<br />
foreigners lured by the American student visa.<br />
如果你身居高位，能够领导科学界，那么你应该尝试着去劝说那些发放研究经费的部门<br />
少招些博士生。大街上论吨撮的科学家完全就是他们的资助政策的后果――几乎所有的<br />
博士生都是由联邦基金支持的）。那些基金会总在抱怨很少有年轻人对科学感兴趣，而<br />
造成这种结果的原因正是他们毁了科学作为事业。他们本可以扭转这种局面，只要他们<br />
少招些博士生，让博士生的人数与教职的人数大致相当就可以了，但他们不干，甚至他<br />
们根本不屑于严肃地讨论这件事（许多年来，NSF到处宣扬他们虚假的预测，说科学家<br />
短缺，而多数基金会好像真以为是这么回事）。结果就是，最好的年轻人，本该去做科<br />
学家的，对此唯恐避之不及；而博士班里是一帮弱弱的美国学生，还有一帮被美国学生<br />
签证所吸引来的外国人。</div>

							
							
							
							
