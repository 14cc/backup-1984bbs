业已经10年了，一直没找到工作。另一个最牛的家伙，35岁，人人都认为他很聪明，博<br />
士毕业7年了才找到一个“永久工作”（其实不是永久教职，只是6 年后有希望获得永<br />
久教职而已，不过这已经让他稍稍远离那种每两年就要找新博士后工作搬家的驴拉磨死<br />
循环了）。还有一个例子，一个39岁的家伙，想竞聘另一个讲师职位。他发了35篇文章<br />
。与之形成鲜明对比的是，一个典型的医生29岁就进入了实习阶段，一个典型的律师25<br />
