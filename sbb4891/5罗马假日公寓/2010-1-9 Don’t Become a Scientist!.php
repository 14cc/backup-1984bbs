奖学金比这个一半还少。用这么点收入你能支持一个家庭吗？嗯，够年轻的小两口住一<br />
个很小的房子。不过我认识一个物理学家，他的妻子把他踹了，因为她是在厌倦了跟他<br />
不停地搬家却一点定居的希望都看不到。当你三十多岁的时候你就会需要更多的东西：<br />
一个大房子，附近有好的学校，以及其他中产阶级生活所必需的设施。科学是一个职业<br />
，而不是一个宗教的呼召，也不是一个贫穷或独身的判决或者宣誓。<br />
Of course, you don’t go into science to get rich. So you choose not to go<br />
to medical or law school, even though a doctor or lawyer typically earns two<br />
to three times as much as a scientist (one lucky enough to have a good<br />
senior-level job). I made that choice too. I became a scientist in order to<br />
have the freedom to work on problems which interest me. But you probably won<br />
’t get that freedom. As a postdoc you will work on someone else’s ideas,<br />
and may be treated as a technician rather than as an independent<br />
collaborator. Eventually, you will probably be squeezed out of science<br />
entirely. You can get a fine job as a computer programmer, but why not do<br />
this at 22, rather than putting up with a decade of misery in the scientific<br />
job market first? The longer you spend in science the harder you will find<br />
it to leave, and the less attractive you will be to prospective employers in<br />
other fields.<br />
Perhaps you are so talented that you can beat the postdoc trap; some<br />
university (there are hardly any industrial jobs in the physical sciences)<br />
will be so impressed with you that you will be hired into a tenure track<br />
position two years out of graduate school. Maybe. But the general cheapening<br />
of scientific labor means that even the most talented stay on the<br />
postdoctoral treadmill for a very long time; consider the job candidates<br />
described above. And many who appear to be very talented, with grades and<br />
recommendations to match, later find that the competition of research is<br />
more difficult, or at least different, and that they must struggle with the<br />
rest.<br />
