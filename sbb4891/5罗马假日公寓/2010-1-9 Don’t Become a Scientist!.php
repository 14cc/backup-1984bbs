是怎么运作的？把这个想法忘了吧！<br />
的确，科学很有趣，也很刺激。由发现而产生的强烈快感是独一无二的。如果你很聪明<br />
、有野心，并且也很刻苦努力的话，你的确应该在读本科的时候选择科学。但这就够了<br />
，到此为止。本科毕业之后，你将必须面对这个真实的世界。这意味着，你不应该哪怕<br />
是考虑去读科学方面的研究生。做点其他的行当吧：医学、法律、计算机、工程，或者<br />
其他随便什么你能想到的。<br />
Why am I (a tenured professor of physics) trying to discourage you from<br />
following a career path which was successful for me? Because times have<br />
changed (I received my Ph.D. in 1973, and tenure in 1976). American science<br />
no longer offers a reasonable career path. If you go to graduate school in<br />
science it is in the expectation of spending your working life doing<br />
scientific research, using your ingenuity and curiosity to solve important<br />
and interesting problems. You will almost certainly be disappointed,<br />
probably when it is too late to choose another career.<br />
American universities train roughly twice as many Ph.D.s as there are jobs<br />
for them. When something, or someone, is a glut on the market, the price<br />
drops. In the case of Ph.D. scientists, the reduction in price takes the<br />
form of many years spent in “holding pattern” postdoctoral jobs. Permanent<br />
jobs don’t pay much less than they used to, but instead of obtaining a<br />
real job two years after the Ph.D. (as was typical 25 years ago) most young<br />
scientists spend five, ten, or more years as postdocs. They have no prospect<br />
of permanent employment and often must obtain a new postdoctoral position<br />
and move every two years. For many more details consult the Young Scientists<br />
’ Network or read the account in the May, 2001 issue of the Washington<br />
Monthly.<br />
为什么我，一个有终身职位的物理学教授，一个在科学事业上很成功的人，要来试图打<br />
击你们将科学作为毕生事业的勇气和信心呢？因为世道变了。我1973年拿到我的博士学<br />
位，1976年就拿到了终身教职。美国的科学界现在已经不能提供一条合理的事业生涯的<br />
途径了。如果你去读科学的研究生，你大概一定会期望着用你毕生的工作精力去做科学<br />
研究，用你的智慧和好奇心去解决那些重要而又有趣的问题。实话讲，你基本上一定会<br />
失望，而失望的时候，你大概已经错过了选择其他任何职业的机会。<br />
