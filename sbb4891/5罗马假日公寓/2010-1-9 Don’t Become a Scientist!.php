大多数的年轻科学家都得当5年，10年，甚至更久的博士后。他们没什么拿到永久职位<br />
的盼头，常常必须每两年找一个新的博士后工作，然后搬家。欲知更多详情，请咨询“<br />
青年科学家协会”或读一读华盛顿大学月报 2001年5月的文章。<br />
As examples, consider two of the leading candidates for a recent Assistant<br />
Professorship in my department. One was 37, ten years out of graduate school<br />
(he didn’t get the job). The leading candidate, whom everyone thinks is<br />
brilliant, was 35, seven years out of graduate school. Only then was he<br />
offered his first permanent job (that’s not tenure, just the possibility of<br />
it six years later, and a step off the treadmill of looking for a new job<br />
every two years). The latest example is a 39 year old candidate for another<br />
Assistant Professorship; he has published 35 papers. In contrast, a doctor<br />
typically enters private practice at 29, a lawyer at 25 and makes partner at<br />
31, and a computer scientist with a Ph.D. has a very good job at 27 (<br />
computer science and engineering are the few fields in which industrial<br />
demand makes it sensible to get a Ph.D.). Anyone with the intelligence,<br />
ambition and willingness to work hard to succeed in science can also succeed<br />
in any of these other p!<br />
rofessions.<br />
Typical postdoctoral salaries begin at ?,000 annually in the biological<br />
sciences and about ?,000 in the physical sciences (graduate student stipends<br />
are less than half these figures). Can you support a family on that income?<br />
It suffices for a young couple in a small apartment, though I know of one<br />
physicist whose wife left him because she was tired of repeatedly moving<br />
with little prospect of settling down. When you are in your thirties you<br />
will need more: a house in a good school district and all the other<br />
necessities of ordinary middle class life. Science is a profession, not a<br />
religious vocation, and does not justify an oath of poverty or celibacy.<br />
