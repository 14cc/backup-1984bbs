<br />
Don’t Become a Scientist!<br />
Jonathan I. Katz<br />
Professor of Physics<br />
Washington University, St. Louis, Mo.<br />
[my last name]@wuphys.wustl.edu<br />
千万别成为科学家！<br />
约拿单 I. 卡茨，物理学教授，华盛顿大学<br />
Are you thinking of becoming a scientist? Do you want to uncover the<br />
mysteries of nature, perform experiments or carry out calculations to learn<br />
how the world works? Forget it!<br />
Science is fun and exciting. The thrill of discovery is unique. If you are<br />
smart, ambitious and hard working you should major in science as an<br />
undergraduate. But that is as far as you should take it. After graduation,<br />
you will have to deal with the real world. That means that you should not<br />
even consider going to graduate school in science. Do something else instead<br />
: medical school, law school, computers or engineering, or something else<br />
which appeals to you.<br />
