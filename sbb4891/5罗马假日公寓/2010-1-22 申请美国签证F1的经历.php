													<h2>申请美国签证F1的经历</h2>
						
						
																											<div id="postmessage_445365" class="t_msgfont">论坛里应该有不少人想要去美国，留学或者移民。F1签证是留学签，有效期一年。<br />
<br />
其实我的案例没有多少值得借鉴的内容。只不过分享一下，以供参考。<br />
<br />
从想要留学到拿到签证历时N多年。但实际上我几乎没有任何准备，羞愧的说，实在是个懒人。<br />
<br />
高中毕业学雅思，雅思成绩才5分。大四学托福，托福成绩惨不忍睹。找了家中介申请了个西海岸的烂学校，主要就是为了拿到offer，先出去了再说。<br />
<br />
08年9月末第一次签证，成功杯具。签证官问我为什么要出国，学什么。后来也知道自己的回答不够完整。<br />
<br />
10年又签时，其实已经放弃了美国梦，只是打算履行和中介合同上的义务，杯具三次，拿回中介费而已。<br />
<br />
依然没任何准备，没语言成绩，没G成绩。其它材料倒是很齐备。工作证明，甚至带了名片。但英语方面很烂很烂。<br />
<br />
中介顾问和我说现在流行汉语面签，我竟然就相信了，每天回旅店就是上网翻墙看twitter。<br />
<br />
面签的是个黑人，当时我就想，歇菜了。他用英文和我打招呼，我就很麻木的回应了他。然后问我学习计划，我没计划，就说去了再说。<br />
<br />
询问我对专业的了解，这个多少还是有点了解了，就随便一说。<br />
<br />
问我工作那公司的主营业务，自己的职责，因为对这块比较了解，所以即使紧张也说的挺明白。<br />
<br />
后来问我回国计划，简单的答完了，就拿到签证了。<br />
<br />
再次强调，英语是绝对有用的，想要出国的一定要学好。我的英文水平仅仅停留在初中阶段，还不知道去美国之后怎么应付。<br />
<br />
另外我记得当时上李笑来老师的课时，他说过思想比语言更重要，这点我也是绝对相信的。其实很多人不知道说什么时，并不是有了汉语的思路，翻译不成英文，而是连汉语思路都没有，所以凭空而起的也不可能有中文。如果有了汉语的基础，英文就是多加练习，学习的事情。<br />
<br />
P.S.与本贴主旨无关，我很想学好英语，并非完全为了next step，主要是和外国人沟通时，不再stupid。有点五毛的说，晚上和一个荷兰孩子聊天，给他讲中国的网络审核，禁止发色情短信等内容，讲完之后我就后悔了，他不会以为中国就是座大监狱吧，然而我却想不出来该怎么告诉他中国普通人生活的还是很“幸福”的。毕竟正常人的思维都不可理解禁止荤段子的事。</div>

							
							
							
							
