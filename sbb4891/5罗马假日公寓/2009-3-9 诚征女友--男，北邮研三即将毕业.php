&nbsp; &nbsp; 人人都自卑。形成自卑的过程大约有两种。一是在小时候跟成人的比较过程中，都有不如成人的深刻体验；再加上某些不太利于成长的环境，自卑的状态就可能凝固在心里。二是每个人对自己的事情都比较了解，对别人的事情比较不了解，在自己的视野下，神秘的那边被不自觉地赋予一些同样神秘的力量或者光环。 <br />
<br />
自卑源于所有生物都具有的攻击性。这种攻击性的呈现方式，就是在心理和行为层面，时时、处处跟他人的比较。比较就是竞争，竞争就是在智力和体力上对他人实施攻击。 <br />
<br />
自卑者的攻击多半是在其内心里完成的。在面对一个他假象的竞争对手时，攻击性自动发射。如果对手显得比自己强大，攻击性就很快朝向自己，便出现自卑者都有的内心世界；自责、压抑、焦虑或者恐惧。这个复杂而迅速的过程，自卑者和旁人通常都察觉不到。 <br />
<br />
表面看来，自卑是瞧不起自己。但是，如果一个人从来没有把自己跟别人比较之后而瞧不起别人，那也就不会有在比较之后的瞧不起自己。换句话说，没有攻击别人，也就不会有技不如人的结果和失败后的自卑了。 <br />
<br />
有一点点自卑，不是一个太大的问题。过度自卑却可能对生活的方方面面都产生不良的影响。过度自卑者的外在表现形式可以完全相反。有些自卑者，毫不掩饰自己的自卑，经常在自己的言行中呈现弱小和胆怯；另一些自卑者，则“自欺欺人”，他们外在的表现形式，确实自鸣得意、目空一切的自傲和自信。 <br />
<br />
跟过度自卑的人打交道，是一件危险的事情。因为他们的极大的攻击性不是消失了，而是转向了自身，一旦有了时机，这些攻击性便会以出人意料的方式攻击他人。我们看到的事实就是，有些人自卑到了经常受欺负的程度，而当他们忍无可忍反击的时候，通常是“一鸣惊人”。 <br />
<br />
