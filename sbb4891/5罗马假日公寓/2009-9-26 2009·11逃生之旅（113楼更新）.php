																											<div id="postmessage_258882" class="t_msgfont">09&#8226;11&#8226;逃生之旅（2）<br />
<br />
这节说说装备的选购和使用<br />
<br />
作为一只老菜鸟，时隔9年后忽然又骑上单车了，忽然就骑了一个不长不短的长途，而且所有装备都是在出发前2周内采购的。骑行归来，对采购，对使用忽然就有了一肚子建议了。<br />
<br />
先说核心装备――单车<br />
<br />
已经说过了，买黄小跑同学的原因主要是郊区锻炼，偶尔跑长途。比如这次11之旅就是偶为之事。买车之前对单车知识知之甚少，看了几天的帖子后放弃了攒车的念头，买整车方便省事。<br />
<br />
采购一事，任何行业任何产品都是一样的：无外乎就是定需求，定预算，定候选产品，交叉比较后作出最后选择。严谨些可以编一个表格出来，把各项需求和功能/标准分值/权重指数等参数列出，然后把候选产品一一打分，最后得分最高的一定就是你要的。<br />
<br />
首先为什么是公路车？第一，我喜欢公路车。第二，不玩越野，速降。第三，绝大多数时间在公路上骑。<br />
为什么这次长途骑行用公路车？第一，全程国道/省道。虽然有某些路段维修或者欠维护，99%以上的路况良好（如果把小石子排除在外的话）。第二，10年前也是一个捷安特公路车跑的北京到呼和浩特，没有出过一点问题。第三，公路车轻便，阻力小，省力，跑得快些。<br />
<br />
为什么是黄小跑同学？第一，好看呀！一路看来，越发觉得黄小跑同学很骚。第二，迪卡侬我多熟呀。号称09-10雪季来临前一举超越凹凸那厮成为迪卡侬最大的粉丝。第三，降过几次价后觉得占了迪卡侬很多便宜。第四，进口车，满足虚荣心，以实际行动抵制萨科齐接见达赖。<br />
<br />
首先确认了自己喜欢骑单车，也决定骑下去，不是一时的头痛脑热，脑子进水的冲动。也肯定了买单车就跟买电脑一样，不会升级不会二手处理（电脑不用了就送人，还落个人情）。所以就直接买这个中档价位的车。<br />
<br />
干的多：6天跑10000公里<br />
<br />
吃的少：只爆胎2次（前后各一），事后证明这2次都只因为出发前气压打太高造成的，外胎并没有刺破，内胎的破洞都在靠近轮圈这侧。<br />
<br />
不挑食：第一次爆胎后，补好装上打气时，不慎将气嘴弄断。只好换上带的备胎。第二次爆胎后，补好后发现补胎技术不过关，还在原地漏气。俺只带了一条内胎，只好换上同伴带的700内胎（黄小跑同学是罕见的650轮圈公路车）试试，木想到呀，也能用，接下来跑了4天，跑得还挺欢。<br />
<br />
手变精准，俗称“稳准狠”，升降档平滑准确迅速。刹车灵敏。咬合紧密，骑行时基本无声。<br />
<br />
当然，这和新车是有很大关系的，期待一年后还有这些优点。<br />
<br />
就是脚蹬忘了换普通的了，这趟骑下来，脚底很痛，还好穿了登山鞋，鞋底够硬，不然肯定D2就在路边换了普通脚蹬。<br />
以上事实证明黄小跑同学是很称职，显得也很专业。当然最最核心的还是人要努力变得专业，而不是车要专业，更不是行头专业。<br />
<br />
长途最佳产品奖――颁给“休息把”同学<br />
<br />
出发前对休息把没太在意，也没使用过，只是觉得很专业，装上去肯定很酷很拉风，肯定秒杀那些个渴望风尘仆仆的旅行者带她们去未知远方的怀春少女和寂寞少妇们。对吾等老菜鸟，休息把非常符合“人不专业时装备一定要专业”的原则，尤其是那些显得酷显得专业的东西，那是一定要装上滴。<br />
<br />
果然，装上休息把的黄小跑同学显得很威，一扫阴柔之气。当然，装备真用上了才真的叫好，就跟男人一样，光好看是不行的，得好用，得让姑娘们用着暗爽，用很俗的话就叫“谁用谁知道”。<br />
<br />
休息把就符合这个条件。<br />
<br />
所以本次最佳产品奖毫无疑问的颁给了休息把同学。一路上大大减轻了手指手掌手腕上臂的压力，而且降低风阻，更省力。当我趴在休息把上，撅起屁股时，就宛如大国撅起，显得分外的专业，很酷很拉风。<br />
<br />
且，锦衣夜行时，休息把上还可安装销魂滴头灯，角度可任意调节。<br />
<br />
记住：装酷也是菜鸟们前进的动力，尤其是老菜鸟。想一想，在温暖潮湿的南方，在杨柳依依的古运河畔的小镇边上，一辆黄色的小跑车以30km/h的速度飞驰而过，你，依偎。这将带走多少姑娘们温柔的目光。<br />
