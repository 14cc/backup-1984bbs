能喝上营养快线一直是我的梦想 每当我在大街上看到喝营养快线的人 ， 我都会偷偷瞄一眼， 真羡慕那些有钱人。前几天我开了工资 ， 在经过激烈的思想斗争之后 ， 我终于下定决的思想斗争之后 ， 我终于下定决心明天去买瓶营养快线喝， 我没有告诉我的父母 ， 他们知道后一定会阻拦的 ， 我激动的一宿没睡。 <br />
<br />
第二天， 我花了2个小时半来到市中心的营养快线专卖店， 我完全被那种气势震住了， 豪华的装修， 我怀着忐忑不安的心情走进了专卖店， 面积达8平米的营业大厅布置和外面一样豪华， 友善而漂亮的服务员热情的接见了我， 可能是太过紧张了 我开口要求试吃， 刚说完我就脸红了 可是服务员并没有感到诧异, 对了， 可能来买营养快线的人都得要求先试喝吧， 毕竟4元/瓶的价格不是说买就买的， 我激动地接过营养快线， 小心翼翼地拧开了瓶盖， 这时的我已经忘了要注意绅士风度 ， 贪婪的喝了一口， 爽滑的口感 不是我以前喝的可口可乐所能比拟的 漂亮大方的服务员热情的向我讲解营养快线的配料和制作流程， 在谈话中我得知她刚从韩国思密达大学毕业主修酒店管理双硕士学位， 回国后她拒绝了去星巴克当店长的机会 来到营养快线专卖店工作， 我也为她的明智选择而感到欣慰， 毕竟进入天天好营养 营养快线专卖店是多少代人的梦想与追求我掏出带着体温的8元钱买了2 瓶。 <br />
<br />
刚要走出店门时服务员朝我喊：“你的营养快线！” 我回眸一笑百媚生回应到： “是你的营养快线。” 服务员当时就脸红了， 我随即以迅雷不及掩耳盗铃儿响叮当之势走出门口 我想 ， 服务员肯定会把所有的营养快线从柜子上都拿下来 一瓶一瓶的数 “他明天会来，他明天不会来……” <br />
<br />
走在大街上我喝着营养快线， 大家都向我投来羡慕的目光， 我也感觉对人生更加有信心了。 <br />
<br />
我一边喝着营养快线，一边思考者人生，一不留神，便撞到了一个人，那人留着和拳皇里东丈一样的发型。我连忙给他道歉，可是那人嘴里挂拉呱啦不知在说着什么，看表情似乎是在骂我。他身旁的同伴也在摩拳擦掌，似乎想要揍我。毕竟是我理亏，我也不想和他计较，便走开了。 <br />
<br />
谁知，他竟然怪叫一声，对我使出了黄金之踵落。这是拳皇里东丈的招式，想不到他也会，看来这人是一个泰拳高手。 <br />
<br />
面对敌袭，我毫不惊慌，毕竟哥也练了几十年的眼保健操。我大喝一声：为革命保护视力，眼保健操第一式，揉天应穴。 <br />
<br />
本来泰拳高手生命力极强大，就算被砍掉四肢都不会死，还可以咬人，但是我一揉之间，破坏全身脉络，立刻就可以把人所有生机全杀掉。这位酷似东丈的人也不例外。 <br />
<br />
看见东丈被我干掉了，他的同伴立刻围了上来。这二三十个人，人人身上都散发出了强大的血气。当前一位，身材又高又大。脊椎挺立，笔直如山峰一般，要刺破苍穹，把天捅个窟窿！我从他的身上，感觉到比奥特曼还强大的气息！一圈圈的光华，从他身上肌肉之中透射了出来，磅礴的战意释放着，四面八方都环绕着肌肉震荡，撕裂空间的狂暴气息。 <br />
<br />
来得好，我说话之间，眼保健操的挤按睛明穴，狠狠施展出来！一道道狂风，缠绕而出。 但是，泰拳高手们在这个时候，相互联手，猛烈的使出死亡龙卷风！十几个泰拳高手同时联手！是何等的威力？足可以惊天动地。 一股狂飙，汇聚在一起，狠狠的撞击向了我，居然一下把狂风撞击的粉碎！<br />
<br />
我轻轻的后退了一步！ “为革命保护视力，按太阳穴，轮挖眼眶 ！” <br />
<br />
面对挤按睛明穴被直接破去，我毫不犹豫的施展出了自己最为厉害的杀招。 泰拳高手们见势不好，又一次联手使出了爆烈飓风猛虎踢 。顿时，漫天的脚影和我的双手撞击在一起。 这一下撞击，并没有分开了，而是死死的枯合在一起，就好像两头牛一样底角。相互比较力量。 一片片的时空塌陷着，一个个的同心圆的时间涟漪，也在不停的发散荡滚着。这是我们死磕带来的力量。 一片片足足有数十里的拳意不停的爆射出去，把整个虚空再次切割得粉碎，周围方圆数千里，乃至万里，都完全成了一狂暴的中心。比起开天辟地以来最混乱的太古，还要混乱一万倍。 <br />
<br />
“是谁在这里扰乱城市秩序？！” 就在这时，一道道黑影悄然的破开虚空，来到了战场之上，显现出了形体来。 这些形体，居然是城市管理者的一个战斗小队！这些城市管理者杀气腾腾而来，但是刚刚一踏出虚空，那无穷无尽的拳意铺天盖地暴走，余波如浩瀚天河断裂扑杀而来。 <br />
<br />
“啊！不好！” 感觉到这威势，城市管理者们脸色骤然变化，就要脱离战场，但是迟了。 扑哧！ 拳意的余波，一下就把他们淹没，他们就好像遭受到了灭顶之灾的溺水者，瞬间被打爆。 毕竟，这个级别的战斗，凡人已经不能插手，城市管理者也不行！ 给我破！我陡然发力，十几个泰拳高手，被一拳击爆！ 肉身化为无穷血雨。 <br />
<br />
