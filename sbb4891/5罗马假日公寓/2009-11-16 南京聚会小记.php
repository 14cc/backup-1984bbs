<br />
陆陆续续有人赶到。看着像70后实际却接近90后的幻水最先赶到，生动演绎了啥叫少年老成。用同一个账号穿一条裤子的两位毒者。留着一撮淫荡小胡子的leonius。隔个几分钟就有人杀到。手机也很忙碌，有人询问路线，有人通知堵车。丙烯携夫人参加，二人尚未领证，丙烯的长相就不说了，但夫人是如此的美丽迷人，一双桃花眼扑闪扑闪，我亲眼看见坐我旁边的电击鱼偷瞄了一眼，然后不住地咽口水。老牌70后帅哥paul以一副浪子的形象示人，不知有几多小女生遭他辣手。<br />
<br />
坐的是长条形的桌子，榻榻米风格，席地而坐。人数刚好，挤得满满当当。15人中，80后占据了大半江山，几位同学的年份不错，出生于1984。另发现党员一枚，camour赶紧辩解：“预备，只是预备。”camour保养得当，一副金丝边眼镜，70后的男人走知性路线，那是刚刚好。camour是人生的赢家，顺利当了爸爸。<br />
<br />
glim兄因家中大小二位领导坐镇，不敢出门。发来短信，遥祝聚会顺利。<br />
<br />
毒者有两位，由于没有邀请码，只能挤着一条裤子，用一个账号登陆小组。在这样艰苦的条件下依然在不断地努力要求进步。精神着实可嘉。在这里，代二位求邀请码一枚，免得二人长期穿一条裤子，容易出事。<br />
<br />
yeyiqun本坐在CC姑娘旁边，但无奈被随后赶到的尼康在中间插了一杠，yeyiqun整晚都用幽怨的眼神凝视着尼康，不知情的人还以为两人暗地里有过一段不为人知的沧桑往事。尼康兄光顾着用他的佳能相机拍照，直接无视了旁边萧瑟的目光。CC姑娘整晚沉默，大概是这样沉静的气质深刻打动了yeyiqun和电击鱼两位老兄，两人已约好私下决斗。祝2位好运。我私下认为幻水兄也有想法，但此人不像上述二位如此沉不住气，应该是打算谋定而后动。我看好幻水兄渔翁得利，“我看好你呦”。<br />
<br />
坐CC姑娘旁边的是jiajia姐，身材高挑、珠圆玉润的jiajia姐一副大姐派头，手里一根女士烟，指点江山，气势非凡。丙烯后来也摸了一根女士烟来抽，妄图模仿jiajia姐的路线，不得不说，真的很失败。<br />
<br />
白牛也爱上了一位姑娘，这位姑娘想必有着丁香一样的美丽，引得白牛兄如此倾心。白牛自称由于看不惯电视台的潜规则而负气出走，但大家一致怀疑白牛也遭了毒手，想必也潜过，并，被潜过。不管怎么说，过去的事情都过去了。祝白牛兄好运。<br />
<br />
走浪子路线的老牌帅哥paul是整晚的star，一手引导了当晚的聚会气氛，从珠峰登山到神州大地的最新局势，包括最新的流行时尚，都侃侃而谈。这大概就是所谓的阅历吧。当晚在不知不觉中干掉了两箱啤酒，上过的菜再上了一遍，昏黄的灯光下气氛动人。<br />
<br />
最后，由于丙烯急着回家和夫人造人，烤鸭党南京支部第一次会议在友好欢快的气氛下圆满闭幕。电击鱼和CC姑娘都住在北京东路附近，急忙表示要送CC姑娘回家。我向电击鱼郑重警示：“哥手里可是有你的号码……”yeyiqun在远处，大概是想用幽怨的眼神杀死电击鱼。<br />
<br />
剩下5人，我，尼康，paul，jiajia姐，camour，一起去湖南路的悠闲美地打了会牌，明暗地主。约莫两小时散去。得尼康兄赠书一本，感谢。<br />
<br />
第一次还算美好。有了第一次，往后就好办了。期待二大，三大，四大的顺利召开。<br />
<br />
以上。是为记。<br />
