																											<div id="postmessage_480049" class="t_msgfont">看了那个“问问数码相机的事儿”的帖子，发现小组内还是有不少摄影达人，而且意见也很中肯。<br />
最近因为买数码相机的事也很纠结，所以小弟特在此请教各位达人，应该如何抉择：<br />
<br />
小弟因为还是学生，也是摄影新手，所以不想急着入单反，但是觉得卡片机又有点太傻瓜，所以想买个类单练练手。<br />
我平时主要拍风景的多，当然人像在拍的也不少。挺爱夜拍、室内拍的也较多，对摄影功能的要求不高。因为不爱看运动所以对运动功能也没什么要求。绝不是变焦党，变焦倍数要求不高。几个因素考虑的顺序是：成像&gt;功能&gt;外观<br />
<br />
目前看中两款机型：富士S205EXR和松下FZ35<br />
<br />
先来说说富士：<br />
1.看中的是它1/1.6超大CCD，感觉成像效果不错<br />
2.EXR模式似乎效果提升明显。<br />
3.广角端差了点，个头大了点，不便携。<br />
4.紫边现象似乎比FZ35要更严重一些。<br />
<br />
FZ35吸引我的主要是：<br />
1.徕卡认证镜头<br />
2.小巧轻便。<br />
3.成像效果也不错。<br />
4.虽然不看中摄影，但是有高清摄影功能总比没有好。<br />
<br />
请教组内相机达人，究竟哪款更适合我呢？或者还有其他的型号推荐，欢迎贴上。<br />
谢谢！<br />
