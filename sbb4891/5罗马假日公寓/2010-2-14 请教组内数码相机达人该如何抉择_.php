再说说他的摄影功能，你可以直接无视之。用处不大，除非你打算用相机拍短片，想当导演直接上DV，拍聚会也没法拍那么长时间，还能干啥？想来想去，只能自拍人体科学片。想必你也无此雅好。我一直坚持是什么东西，就当什么东西用，一机多用到最后一定是什么也用不成。<br />
成像，这是个问题。松下的成像也就是能看。我说的能看的意思是在液晶屏上看看得了。想出片子的话，除非你的P功过硬，不然还是免了吧。<br />
3/4微系统，我们圈子里都称之为伪系统。一直没想明白奥巴里是哪个天材想出来的点子，可以媲美“搞笑诺贝尔奖”了。换句话说，上手松下，你可就直奔着奥巴和松下给你挖的坑直接一去不回头了。<br />
<br />
富士，没用过。没法评价。反正单反备机圈里没听过这一号。<br />
<br />
G10或是G11可以用，但前提是在外出的时候能保证有三块以上的电池。<br />
<br />
DP1、DP2不错，恒定光圈，画质牛比。拍人像说得过去。风景也可以，广角+大光圈，还想什么呢？<br />
<br />
再说说你的定位问题，按你的想法，我认为直接上单反是最好的选择。不管是C、N、S产品线都很成熟，可以选择的余地大。用正经的单反好好练练手，以后有机会上全幅就会从容很多。举个不是很恰当的例子，如果你现在拿的是单反，卡片机上手就是一会儿的事，如果你现在拿的是卡片，单反机上手可就不容易了。单反和卡片真的不是一道劲。<br />
<br />
如果你非说我铁了心就是要先上卡片再上单反，推荐DP1、DP2和富士的GRD 2或者GRD 3。<br />
