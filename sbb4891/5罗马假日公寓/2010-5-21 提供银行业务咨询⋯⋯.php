																											<div id="postmessage_634848" class="t_msgfont">费用的列支并不是以取得发票与否来确认的。企业所得税法明确规定，与生产经营无关的不能税前列支，这才是列支的标准。如果拿其他无关的发票列进去，就是征管法六十条规定的虚列成本费用。<br />
