<br />
企业所得税税率目前已经是全国统一、内外资统一，基本上没有区别（除了一些过渡政策），所以换注册地或是改变性质已经不起作用，而广告业在所得税上是没有什么优惠的。<br />
<br />
关联交易在国内其实意义不大，因为一是所得税税率已经统一，转来转去一样少不了。二是所得税法也给了税务局调整的权力。<br />
