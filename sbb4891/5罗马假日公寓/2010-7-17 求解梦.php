<br />
　　有一位结婚多年未能怀孕的妇女，梦到自己怀孕，这是梦境“日有所思，夜有所梦”的充分体现。这是非常正常的，但却建议做梦者尽量放松精力，因为太紧张的话对妊娠也是不健康的。<br />
