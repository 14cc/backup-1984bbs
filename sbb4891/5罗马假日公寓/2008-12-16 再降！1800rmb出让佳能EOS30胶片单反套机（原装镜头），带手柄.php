																											<div id="postmessage_10771" class="t_msgfont">2001年在报社的时候，从西单金广角买的，当时价格是5600左右，镜头1600多左右，手柄不到800。合计7900。～～从没回忆过，难为我一想就想起来了，还记得这么清楚准确。<br />
2003年离开报社以后，用的就很少了。如今更是如此，放着也是放着，不如让给喜欢或者有用的人。<br />
<br />
眼控对焦、七点对焦啥的是这个相机当时推出时的新功能。<br />
镜头是套机的标准配置，24-85，光圈3.5-4.5，后配的镜头盖。<br />
<br />
还有一个手柄。<br />
送半旧相机包一个。<br />
合计2000元转了。――再降，1800元出手！<br />
<br />
百度搜的特点一览 <br />
⊙EOS 30是取代EOS 50（于1995年推出）的业余机种 <br />
⊙EOS 30是更快捷的眼控自动对焦（Eye Control） <br />
⊙七点对焦（左右五点，上下两点）配置同EOS 300 <br />
⊙E-TTL多灯自动闪光 <br />
⊙13项用户自选功能 <br />
⊙出色的跟踪和预测对焦能力 <br />
⊙连拍最快每秒4张（动体预测对焦为每秒3.5张） <br />
⊙新开发「WhisperDrive」过片系统让过片噪音降低&nbsp;&nbsp;<br />
⊙35区分割测光系统 <br />
⊙视度调节机构、独立预观景深钮兼备 <br />
⊙丰富的新款附件 <br />
⊙使用CR123A电池两枚 <br />
⊙市场行情价约在人民币4000.00元 <br />
 <br />
精巧实用的造型设计<br />
您肯定无法忘记握住一架佳能相机时稳固而轻松的手感。为了让相机更加小巧，与EOS 50E相比，EOS 30/33相机光轴到手柄的距离减小了4.4mm。另外，相机手柄的形状、快门钮和电子拨盘的布局都经过了再次优化，以提供更好的操作性和更舒适的手感。 <br />
<br />
在眼罩、快门钮和电 子拨盘附近,都有像 Eos-3一样的磨沙处理,用户的手持 部分都使用了柔软的橡胶表面,整个 外观设计拥有出色的整体感和舒适的 手感,就像为您度身定做的一样.指 令拨盘和自动对焦模式拨盘都具有栅格花纹,让用户可以轻松掌握. 在速控拨盘内新增加了对焦点选择键,可以轻松、迅捷地在7个对焦点进行选择.同样还新增的独立眼控开关,这样，机顶一共有3个拨盘作为操作界面，简洁而直观。<br />
<br />
快捷的7点对焦<br />
为了帮助用户更好的完成构图，尽量减小焦点锁定操作的麻烦，EOS 30/33采用了7点CMOS自动对焦传感器，通过SI叠加系统，7个对焦点可以清晰地显示在取景器中，您可以使用自动、手动和眼控三种方式来选择对焦点，由于新增加了4个方向的对焦点选择键，手动选择变得更加方便快捷。&nbsp;&nbsp;<br />
 <br />
更快捷的自动对焦速度<br />
EOS 30/33采用了佳能最先进的对焦技术,在单次对焦模式下,EOS 30/33的自动对焦速度和精度与E0S-3相同;在预测对焦模式下,装有EF300mmf2L IS USM镜头的EOS3O/33可以追踪一个以5O公里时速接近相机的移动被摄体直至离相机8米处。（与EOS-1V和EOS-3的性能相同）<br />
<br />
Eos30/33内部有两块微处理器,其中负责自动对焦的是主频为33.554MHz的32比特微处理器,它的指令执行周期只有0.03毫秒,这使得Eos30/33在对焦点自动选择模式下的自动对焦速度与Eos-3-样 快。为了提高人工智能伺服模式下的自动对焦速度,EOS30/33改变了Eos50E的操作时序。EoS50E在完全按下快门钮后,镜头立刻停止对 焦操作,反光镜翻起,电子光圈收缩,然后进行曝光.在EOS30/33上当您完全按下快门钮时,镜头依然进行对焦操作,其他时序不变,反光 镜翻起，电子光圈收缩,快门开启前,镜头才停止对焦操作。这样，就 大大降低了预测对焦与实际位置的误差,提高了整体速度。<br />
<br />
同时，为了使连续拍摄时卷片速度一致EOS30/33也使用了类似 Eos一1v的镜头驱动待机技术,在每次自动对焦完成后插入一段待机时间,补偿了镜头驱动时间的差异,让连拍速度恒定,拍摄更加顺畅。 <br />
 <br />
得心应手的眼控对焦选择<br />
32比特微处理器的运算、四个IRED的眼控检测.以及增强的眼控检测算法让E0S30的眼控性能比EOS-3还快、还精确.实际上,它的眼控反应速度是EOS50E的两倍.它采用了和EOS-3相同的70O0像素区域BASIS传感器,反应时间仅为55毫秒,是目前最快的。EOS30可以存储5组眼控数据,由于速度的提高，进行眼控校准所耗费的时间也缩短了。Eos30采用改进的自动对焦算法，在全自动模式下眼控开关打开时,对焦点选择算法会结合眼控信息进行对焦点自动选择。每个对焦点的选择优先级也根据这些信息而变化.由此大大提高了选中用户希望的对焦点的概率。<br />
<br />
一目了然的取景器设置<br />
按照佳能一贯的产品设计的延续性,E0S3O/33的取景器设置和EOS50E 非常相似∶五棱镜取景、固定对焦屏、90%垂直覆盖率和92%水平覆 盖率、0.70倍的放大率(50毫米镜头对焦于无限远处)、19.5毫米的眼 点距离,以及信息显示的细节等等都一样。但是E0S30/33增加了 -2.5至+0.5的屈光度调节功能,即使眼罩安装在上面也可以轻松设置。 由两片不同材料组成的屈光度调节透镜组出色地补偿了色差,让取景器 成像真实锐利.其次-由于改进了驱动马达机构,快门开启瞬I同取景器 的遮挡时I同比EoS50E减小了20%,提高了抓拍的能力. 和Eos5ˉ样,Eos30/33使用了五棱镜前发光二极管叠加对焦点显示。 为了显示上下两个新增的对焦点,田S3。门S便用了第二条叠加光路, 让对焦点的显示非常滑晰明了。<br />
<br />
丰富的测光与曝光功能<br />
与7点自动对焦相适应,EOS30/33采用与对焦点联动的35区评价测光 系统,另外还可以提供占据画面中央约10%面积的局部测光，以及中 央重点平均测光。其基本的测光规格与EOS50E相同。<br />
<br />
与EOS50E相比,EOS30/33拥有更加全面的曝光模式,它增加了夜景 模式X-TTL评价闪光曝光可以实现全面的闪光控制.550EX闪光灯MR- 14EX环形微距闪光灯装在EOS30/33上时,E-TTL自动闪光、闪光灯曝光锁定、焦平面闪光、无线闪光控制（最多为3个从属组）、闪光光比控制和造型闪光等先进功能都可以实现。同时，相机内置的E-TTL自动闪光传感器也可以和EZ系列闪光灯兼容。<br />
