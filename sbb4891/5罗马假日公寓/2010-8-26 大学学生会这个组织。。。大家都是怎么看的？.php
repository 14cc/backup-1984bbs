													<h2>大学学生会这个组织。。。大家都是怎么看的？</h2>
						
						
																											<div id="postmessage_798310" class="t_msgfont">学生会<br />
 学生会（Student Union）<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 高等、中等学校中学生的群众性组织。任务是，在学校共产党组织的领导和共青团组织的指导和帮助下，团结全体同学，使他们在德智体方面得到全面发展。高等学校一般设校学生会委员会和系(科)学生会委员会两级。中等学校学生会一般设校学生会委员会和班学生会委员会(班委会)两级。<br />
<br />
&nbsp; &nbsp; 它是每个大中学校不可缺少的部门（少数大学，如中国防卫科技学院除外），它是提倡自我服务，自我管理，自我学习的学生组织，为学校学生和老师提供无偿服务的部门。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 一般的学生会都设置主席团、秘书处、外联部、学习部、企划部、宣传部、纪检部、文艺部、体育部、生活部、卫生部、劳动部、组织部等。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 中等学校和高等学校中学生自己的群众组织。1919 年五四运动时期，中国中等学校和高等学校学生成立了进步的学生团体，称学生自治会，对学生运动起了积极作用。中华人民共和国建立后，改名学生会。凡在校学生都是学生会的当然成员。学生会的干部经全体学生民主协商，选举产生。<br />
&nbsp; &nbsp; 中等学校学生机构一般是两级：校学生委员会和班学生委员会（班委会）。高等学校又有院系（科）一级的学生委员会。全国高等学校建立的学生会联合组织为中华全国学生联合会，简称全国学联。<br />
&nbsp; &nbsp; 学生会的任务是在学校共产党组织的领导和共青团组织的指导和帮助下，团结全体同学，贯彻党的教育方针。引导同学努力做到身体好、学习好、工作好，德、智、体、美 、劳全面发展，成为有社会主义觉悟的、有科学文化知识的 、身体健康的劳动者。<br />
<br />
<br />
学生会的起源及历史<br />
<br />
<br />
清华于1919年12月23日成立了第一个学生会，它诞生于“五四”运动爱国、民主、科学的洪流中。此前清华学校初期，还没有全校性学生会组织。<br />
<br />
&nbsp;&nbsp;在1919年的“巴黎和会”上，美英法日等国，无理地拒绝了中国人民“废除二十一条”的正当要求，还公然把德国在山东的权益全部让与日本。丧权辱国的北洋政府竟同意在和约上签字。消息传来，举国愤慨，1919年5月4日北京城内3000余名学生举行了反帝爱国示威游行。当晚，五四运动爆发的消息传到清华园，一向沉静闭塞的清华学校顿时沸腾起来。高等科二年级学生闻一多连夜工笔抄录了岳飞的“满江红”词，贴在高等科饭厅门口，表达了“从头收拾旧山河”的决心。第二天一早，有些同学在宿舍门口贴出红笔写的大字报，要求立即行动。当日（5日）高等科和中等科科长立即召开全校各会社负责人联席会议，决定成立清华学生代表团，派代表与各学校互通声气，与北京学生采取一致行动。清华学生代表团于5月7日正式成立,代表团团长为顾德民，清华学生代表团随即领导清华学生投入反帝爱国运动。5月9日，清华同学在体育馆前举行了“国耻纪念会”。会上决议通电巴黎，要求中国代表拒绝在和约上签字，并庄严宣誓：“口血未干，丹诚难泯，言犹在耳，忠岂忘心。中华民国八年五月九日，清华学校学生，从今以后，愿牺牲生命以保护中华民国人民、土地、主权，此誓。”<br />
<br />
&nbsp;&nbsp;当时全国各地纷纷进行抵制日货的活动，1919年11月15日，日本驻福州领事馆竟派出便衣警察和浪人殴打参加抵制日货活动的学生；16日，又打死打伤学生和市民多人，制造了“福州惨案”。国人闻之莫不愤恨异常，福建罢市、罢学，京津沪等学界也奔走呼号予以声援。27日清华各级学生重选代表，商议起草学生会章程，“以便重组代表团与京中学界共同进行”；并于29日组织清华学生赴城内与各校学生上街游行。12月17日，全体学生大会通过了学生会章程；在23日晚，清华全体学生聚集在饭厅，举行学生会成立大会。清华学生连续参加学生运动引起了学校当局的不满，校长张煜全竟派巡警干涉学生会成立，乘学生开会之际派人关掉电灯以迫使学生散去，想不到此举激怒了学生。在几支洋蜡烛的照耀下，清华学生会庄严成立。学生会设评议部和干事部,由刘驭万、罗隆基为评议部正、副主席，时昭泽为干事部主席；干事部下设总务、交际、文牍、会计、新闻五科，其中文牍科正副主任为闻一多、潘光旦。随着学生会的成立，学生活动日益活跃，校内刊物《清华周刊》也由学校派定学生编辑改为由学生自选编辑，各种学生团体如雨后春笋般兴起。<br />
<br />
&nbsp;&nbsp;清华学生会初期的组织形式模拟的是资产阶级议会政治，即“三权鼎立”。评议会是全校代议机构，行使立法权；干事部行使行政权；司法的权力还掌握在学校斋务处手中。随着“五四”以来新思潮对学生影响的增长，学生们对于校方家长式的严厉管束越来越不满。1919年10月，一位姓李的斋务长因开除学生，遭学生诘问并被打。同年12月，校长张煜全因干涉学生会成立大会，遭到学生反对而被迫去职。学校传统的管理方式被动摇，1920年起学生对自治展开广泛讨论，认为“学生自治，是由学生组织团体去练习学生管理自己”。到1922年3月，学生通过学生会提出要求，成立了学生法庭，选出了审判官和检察官， “三权鼎立”随之形成。学校还拨了一笔钱，为“法官”们缝制了“法服”。穿戴上峨冠博袖的服装，学生法官们审理学生间争执事件甚至处理学生违反校章的事件。1925年学生法庭被停止。1929年《国立清华大学学生会章程》规定“本会最高权力机关为全体大会，立法机关为代表大会，执行机关为执行委员会”。<br />
<br />
