													<h2>童鞋们办理二代身份证的时候有没有遇到这种情况啊?</h2>
						
						
																											<div id="postmessage_625143" class="t_msgfont">在辽宁省政府纠风办和纪委的网站(<a href="http://www.mxwz.com" target="_blank">www.mxwz.com</a>)上投诉大连开发区哈尔滨路派出所来着,过了一个星期总算有电话打来询问情况了.问了些基本情况,没承认态度有问题,只说警察方面遵守规定干得没问题.<br />
&nbsp; &nbsp;&nbsp;&nbsp;这个链接是警察放下电话后我拨回去的录音<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; <a href="http://smth.org/att.php?p.722.284486.4093.amr" target="_blank">http://smth.org/att.php?p.722.284486.4093.amr</a><br />
&nbsp; &nbsp; 想了解一下其他地方是否也有遇到类似问题,印象里学生毕业后很多人都有一段时间挂靠集体户口的<br />
&nbsp; &nbsp;&nbsp; &nbsp;有这方面经验的可以给俺指导指导,投诉哪里更好使些<br />
<br />
<br />
内容如下:<br />
 <br />
<br />
&nbsp;&nbsp;受理编号： 167011&nbsp; &nbsp;<br />
 <br />
<br />
<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;关于本人办理二代身份证过程中遇到的一些情况<br />
<br />
&nbsp; &nbsp; 本人为大连经济技术开发区集体户口,集体户口个人页(常住人口登记卡)在本人处保存,集体户口首页因原工作单位解体,无法联系取得.<br />
&nbsp; &nbsp; 因工作原因,本人未能在集中办理二代居民身份证过程中及时办理新身份证,故近日去开发区哈尔滨路派出所办理二代身份证.办理过程中遇到一些问题，从个人感受上讲，我认为大连开发区哈尔滨路派出所存在服务态度差，故意刁难，主动设置障碍造成当事人难以办理相关手续甚至借以制造权利寻租机会的问题。需要提前说明一下的是，过去几年中,因本人不慎,曾发生过两次身份证遗失,均持常住人口登记卡赴户口所在地派出所(大连开发区哈尔滨路派出所)核对户籍信息后顺利进行补办，未发现派出所工作人员存在服务瑕疵.<br />
&nbsp; &nbsp; 今次本人办理身份证过程中，负责办理身份证业务的警员在能够通过户籍管理系统确认本人户籍信息的情况下，坚持要求提供集体户口首页，当了解到本人集体户口所在企业已注销无法取得集体户口首页后并未提示应如何进一步办理手续，反而诱导性询问本人是否仍在集体户口所在地址居住（事实上因传统原因，集体户口中的地址常常是XX路1号等企事业单位办公所在地，根本不住人。况且使用集体户口的公民不住在户口所在地的十分平常），该警员立即提出我必须到目前居住地居委会和派出所开具暂时挂靠等若干证明材料以及工商局提供的原单位注销等各种手续，且对相关手续是否存在其他简便办理途径或办理必要性没有任何解释。后经本人咨询他人，了解到出具集体户口首页的主要目的是常住人口登记卡上不能体现派出所公章和地址信息，于是通过户籍警在常住人口登记卡上加盖了公章并签注了户口地址等信息。但办理身份证的警员仍坚持认为本人手续不完整，不予办理二代身份证。<br />
&nbsp; &nbsp; 后来本人询问该警员:&quot;假设本人身份证遗失，目前居住街道不予办理相关挂靠等证明的情况下，又或者本人当晚搬回户籍所在地，难道就无法补办身份证了么？&quot;该警员以严厉口吻表示：“你说你搬回去了，你拿什么证明你搬回户口所在地？”在此情况下,本人只能暂时停止办理业务离开该所.考虑到南方一些省份为身份证换代提供的便民措施，例如广东省已经在推行网上自助办理，大连开发区哈尔滨路派出所在办理二代身份证时的工作态度简直匪夷所思。<br />
&nbsp; &nbsp;&nbsp;&nbsp;需要额外说明的是,整个办理过程中,本人态度甚为谦恭,并无任何言语冒犯,而相关警员的态度始终倨傲冷漠。特别是针对办理业务的当事人的诱导性询问，据我观察，在本人前面排队的6个人中，至少有3个人的手续较为齐全却因受理警员的诱导性提问而被提出补充要求，使得当事人无法继续办理，需要很费周折才能进一步满足要求。同时现场警员不针对当事人进行更多解释。可以合理的认为 ，即便当事人认真按照要求补充相关手续，下次办理时仍很可能被刁难以其他问题继续拖延办理。总的来说，该派出所警员的工作态度较以往本人补办身份证时有了很大变化。<br />
&nbsp; &nbsp;&nbsp;&nbsp;另外据悉，开发区的警察在工作待遇方面存在一定的不满，去年甚至发生了部分在职警员在开发区管委会等政府机构前集体上访的情况。同时，就我本人在开发区的原同事和朋友之间询问的情况，大部分情况下，大家（包括我本人）在哈尔滨路派出所办理各种业务最终都是托人甚至使些好处后才顺利办理的。可见我所遇到的问题不是个性问题。据了解，这类刁难百姓的公安干警其实在队伍内部是很受欢迎的，因为这样的人越多，老百姓越要去求公安办事，无论是公安干警的“社会地位”还是权利寻租的机会都大大增加。<br />
&nbsp; &nbsp; 就本次的情况而言，托在公安系统里的亲戚朋友打招呼把身份证办下来或许可以，但在该派出所办理业务的遭遇却不吐不快。本人从事互联网管理工作，有一定的网络舆论引导经验，也了解民心网是辽宁省内督办民众问题比较有办法、有力度的机构。考虑到通过民心网投诉反馈问题可能导致未来我办理身份证或迁转户口过程中受到该派出所工作人员的进一步刁难，本人不排除建立专门的网站用于搜集整理广大民众在同公安打交道过程中的经历，在网络上形成一定程度的言论压力来推动本地政府提供便民服务的可能。<br />
&nbsp; &nbsp; 我不求本人的个案因为民心网的督办而得到解决，只希望下次去办理业务时能够看到具有起码敬业精神，态度温和的为民众提供方便快捷服务的人民警察。<br />
