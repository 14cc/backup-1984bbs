胶片的宽容度目前高于数码成像元件，但仍然比正常人的瞳孔成像的宽容度窄；即便是我们夸嘘人类的视觉对于色彩和亮度的宽容度有多高，但是这种宽容度仍然远低于我们使用仪器所能观察到的自然光的细节。而最主要的是，正是这种器材的狭隘性造就了这门艺术。<br />
<br />
在我们欣赏打动人心的照片时，我们看到的是一个色彩与光亮明显具有片面性的作品，这种片面性实际上与作者的主观选择存在密切的联系，换句话说，也就是作者故意选择了某一片段的色彩与亮度，而故意抛弃了其他的部分。同一场景、同一时间，不同的作者会抓住不同的片段来折射作者的的经验，这种片段不仅包括色彩与光亮，同时也包括角度与内容。因此，你不必一开始就去寻找那种正确的曝光值，你应该去寻找你自己内心的触动与灵感。<br />
<br />
对于曝光值的估算，这其实是一门简单的技术，但他往往成为对摄影者最大的约束，这种约束往往天长日久成为一种心灵的枷锁，你想要真正的学会摄影就不应该将曝光估算置于首要，应该更多的去关心内容，关心你的眼睛与心灵。<br />
<br />
