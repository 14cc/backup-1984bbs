一个人的生活只需要一台电脑就够了(2010-02-19 10:50:49) <br />
<a href="http://blog.sina.com.cn/s/blog_476f46d50100gpea.html" target="_blank">http://blog.sina.com.cn/s/blog_476f46d50100gpea.html</a><br />
<br />
既然什么也干不了，我决定用这段时间来骂一骂惠普电脑。<br />
<br />
话说我大年初一就遇到了一件最可怕的事，我的电脑屏幕上出现一行字：电脑屏幕即将进入休眠状态，然后就永远地黑屏了――以前这种情况也发生过，我买回这台惠普电脑十六天，也是一开机就休眠，我打电话去惠普的维修热线，我记得那次的电话打了足足两天，好脾气的工程师告诉我出现这种情况有三种原因，一是静电，二是软件，三是硬件出问题，最后给出的结论就是把边接线换个方向，后来我换了方向，还是不行，工程师永远在那头说我们不提供上门服务，你自己再修修，把我气得当场吐雪，我按你们的指示做了，就是不好，为什么你们让一个台买回来十六天的电脑罢工，我打遍了电话，所有的人都说我们没责任，苏宁要我找惠普，惠普要我找苏宁，最好我妈去了苏宁大闹，卖电脑的人上门给换了一台，捣估了老半天，总算是能用了。<br />
<br />
但我一直在害怕，害怕这情景重演，如果说有恶梦，我的人生恶梦里就有这么一个，<br />
<br />
果然大年初一就给上演。<br />
<br />
有了上一次的电话印象，我这次不生气了，一直等到初六才开始打电话，大过年的，咱们也不能太那个，初六打，还是不行，这次打电话又是一样，上海的惠普永远没有人接，忙音，打通了要我输入机器型号，输了，说对不不起是错误，然后就再见了，我好不容易找到广州的维修处，维修处说不好意思，你要打上海的电话，他们下单了，我们才能修，我再打，又打不通，气疯了，开始咆哮，终于派人来，之所以上次不派人来，是因为才买十六天不能要钱，这次问说你有没有超过一年，我高兴地说超过一年了，您来吧，我愿意出钱――没超过一年谁理你。<br />
<br />
最终，我搞了一台以前的手提上网，这台机子叫FUJITSU，虽然慢，虽然什么也干不了，但用了这么多年，从来没有出现过黑屏这种情况，以前的联想和七喜都没有――在我的使用经历里，惠普是最差的，是谁给我推荐惠普的，啊！<br />
<br />
昨天我看曾老师寄我的书，那个分众的江南春装十三地说，其实，人的生活只需要一台电脑就够了。<br />
<br />
我心中一动，如果一个人的生活只需要一台电脑就够了，请记住，那绝对不是惠普电脑。<br />
<br />
永远不要买惠普电脑，一种美国式清坚加中国式的油滑最后便造就了就服务的极端的不负责任。</div>

							
							
							
							
