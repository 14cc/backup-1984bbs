2002-06-07 09:17:13　　南方网－南方都市报　叶克飞<br />
<br />
 <br />
??在马岛海战之前，英格兰与阿根廷在球场上瓜葛不多。1962年世界杯，他们曾在小组赛中相遇，英格兰队以3比1获胜。让人印象比较深刻的是1966年世界杯的1／4决赛，最终夺冠的英格兰队取胜。阿根廷队的中场队员安托尼奥・拉丁因为与裁判争执而被罚下，比赛为此一度中断达十分钟。终场前12分钟，赫斯特的进球终于打破僵局，将英格兰队送进了半决赛。赛后的一个小插曲也值得一提：在终场哨吹响后，英格兰历史上的功勋教练阿尔夫・拉姆齐迫不及待地跑上场，可别误会，他并非要与弟子们庆祝胜利，而是阻止他的球员与对手交换球衣，理由是“我们不和动物交换球衣”。<br />
<br />
??1986年世界杯――――马岛战争的四年后，马拉多纳几乎是靠一个人的力量帮助阿根廷队捧杯。在1／4决赛中，阿根廷人积蓄了四年的仇恨在这90分钟内爆发，马拉多纳先是用手把球打进了网窝，更难以置信的是进球竟然有效。这所谓的“上帝之手”其实就是复仇之手。在复仇之手后，马拉多纳进了一个世界杯历史上最精彩的入球：个人带球狂奔五十多米，连续突破英格兰队五名球员的防守，然后把球打进网窝，这粒经典进球足以让所有阿根廷人狂出一口气。莱因克尔为英格兰踢进了一个安慰性的入球。<br />
<br />
??“那不是我的手，那是上帝的手”――――马拉多纳在赛后的这句话已被录入了英国足球教科书，成为每一位即将面对阿根廷队的英格兰球员必须熟记的功课。<br />
<br />
??1998年世界杯，英格兰和阿根廷再次相遇。那时的英格兰组建了一支典型的英式打法球队，防守固若金汤，进攻能力却有些欠缺。阿根廷在小组赛中也让人们见识了其稳固的防线。但就是这两支球队，打出了那届世界杯上最精彩的比赛，双方在上半时踢进了四个球。比赛最终进入了加时赛和点球大战。<br />
<br />
??年轻气盛的贝克汉姆为自己的任性付出了代价，他在上半场就被罚下场，这也使得英格兰队在点球大战中输球。球技远逊于马拉多纳，狡诈却有过之而无不及的西蒙尼亲手按倒了贝克汉姆，即使哨声响起，他还是对准贝克汉姆的腰来了一肘。贝克汉姆愚蠢的回敬了西蒙尼一脚，结果被红牌罚下，成了英格兰队失利的最大罪人。（编辑：鸽子）</div>

							
							
							
							
