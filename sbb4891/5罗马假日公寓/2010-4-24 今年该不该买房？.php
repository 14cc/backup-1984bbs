　　1 银行很好啊。包赚不赔。<br />
　　2 开发商源源不断建房子，带动其它行业欣欣向荣。<br />
　　3 炒房人把多余的钱变成房子，不去其它地方兴风作浪。<br />
　　4 愚民老老实实，勤勤恳恳把毕生血汗换成砖头瓦片。为了还房贷不敢辞职，不敢偷懒。<br />
　　5 多印钞票会通货膨胀？目前不会，目前出口受阻，国内生产能力过剩，正好抵消了通货膨胀的压力。一般劳动者就是贱。<br />
　　降房价的坏处：<br />
　　1 银行之前的贷款会出现坏帐。<br />
　　2 开发商热情降低，因为不能用房子套钱后，房子暂时是过剩的，毕竟已经空置了这么多。不过，目前投资实业的利润可能只有10%，相比之下，开发商还是能保持比较高的利润。所以，开发商永远都不会缺。房子是什么？不就是砖头水泥吗？<br />
　　3 之前的炒房人（有多余资金的人）会去炒股，炒其它流动的东西。<br />
　　4 愚民住房压力降低。开始不本分了。不过可以拉动其它消费。<br />
　　5 其实房价降了才更可能通货膨胀，因为真正购买力，特别是基本生活品的购买力提高了。两极分化时候通货膨胀是不高的，有钱人只会买奢侈品，没有哪个有钱人买一车大米囤积。<br />
　　<br />
　　对未来的预测：<br />
　　1 如果不降低贷款的比例，如果贷款额没降到接近建筑成本。房价很难降下来。因为即使没有人买房子，开发商也能通过马甲卖给银行，得到低息贷款。<br />
　　2 在这过程中，真正自住者买房会越来越少，因为真正自住者一般只买一套，所以，房子的空置率会越来越高。这会影响房子租金。其实，租售比真是一个很好的指标。<br />
　　3 这种开发商和银行（政府）勾结的把戏会有越来越多的人熟悉，真正自住人买房的热情会下降。<br />
　　4 中国老龄化和独生子女政策会减弱对房子的需求，这点估计在10年后显现。<br />
　　和平饭店<br />
　　总结：<br />
　　1 就象之前说的，使房价回归理性的方法非常简单。<br />
　　降低按贴比例：这是最根本的一招。一针见血，立竿见影。<br />
　　 最根本的贷款额就是：房子的建筑成本，加一点点实际的地价。<br />
　　2 在政府的立场。目前用这招的必要性并不是那么迫切。<br />
　　毕竟现在房地产能带来不少好处。而那些房奴，也并不是那么悲惨到要命的程度，（要当房奴也要交得起几十万的首付啊，呵呵 所以，他们不会饿死）<br />
　　3 真正自住人，能不买尽量不买，别人买房是要赚钱的，你们买房是留一辈子的，这是真正的冤大头啊。<br />
　　4 炒房人，呵呵，自己看吧。你成本再低也低不过开发商，见好就收吧。<br />
　　5 买房出租的：。。。尽量卖了吧，出租绝对是亏本。<br />
　　6 中国房价的拐点：银行大幅降低贷款比例的时候。<br />
　　<br />
　　 和平饭店<br />
　　<br />
　　不好意思意思，看来有人误会了我说的最终价值的意思。<br />
　　<br />
　　最终价值 我指的是：最本质的价值。最原始的价值。<br />
　　而房子的价格，肯定是最终价值 加上各种附加值来构成的。<br />
　　<br />
　　但是，各种附加值，从某种意义来说，就是泡沫。<br />
　　<br />
　　<br />
　　例如：一个不知名的机器，对于一般人来说，最终价值就是废品站对它的收购价格。 只要低于这个价格，有多少我买多少，因为最坏的结果就是转手给废品站。<br />
　　对于生产它的人来说，最终价值就是它的生产成本。不包括利润，品牌。如果价格都低于成本，那就没有人愿意生产。如果价格高于成本，越高生产他的人就越起劲。现在开发商就是有个银行作为保障，即使没有人买他的房子，银行都会以远高出他成本的价格来收购。这才是他可以肆意妄为的根源。<br />
　　<br />
　　所以，当没有银行这个后盾，房子就只能靠市场来调节。炒房人买房子也只能贷到很少的钱，炒房人的资金就是自己的真金白银，就是自己的肉，就不会这么大胆了。<br />
　　<br />
　　而真正自住人，他本来就不准备把房子再变现的，所以他就没有这种担心。<br />
　　<br />
　　近来房价会不会跌？基本不会！<br />
　　<br />
　　我之前有分析了，从一个政府的角度，要压房价的必要性并不是很大。因为还没有到要死人的程度。<br />
　　<br />
　　房奴，只是辛苦点而已。<br />
　　<br />
　　只要一天 银行不降低贷款比例，房价一天就不会大跌。<br />
　　<br />
　　但是，炒房人注意了， 现在的价格，已经不是一般真正自住人能接受的了。炒房人不是开发商，没有成本优势，炒房人真正到手的只是房子，还要承担贷款利息，虽然这利息相对低，但是开发商不同，他们到手的是钱，可以钱生钱。炒房人的房子，即使出租也受益很少。<br />
　　不用多久，开发商给炒房人的优惠会越来越大，可能是表面标价的7折甚至以下，特别是给大户。为什么？因为炒房人越来越难找到接手的人了，他只能7折进货，然后对外面说亏本了，8折卖出。<br />
　　<br />
　　不过，房子表面的价格不能降。为什么？因为要根据这个价格去找银行贷款。明白吗？实际价格可以降，但是给银行看的价格不能降。因为房子最终都是要靠银行来收购。只有哪天银行减少贷款比例了，价格才可能恢复实际价格。<br />
　　<br />
　　银行是不是最惨？中国的银行不是，只要银行的国有性质不变，银行就高枕无忧。贷款越多，利润越多。只是，背后的货币滥发，由全中国人民买单罢。不过，现在生产力过剩，暂时还不会引起通货膨胀。<br />
　　<br />
　　所以，房价怎么会跌呢？<br />
　　<br />
　　越来越高，自到真正自住人越来越少（所以，现在要加把劲忽悠真正自住人去接盆），差不多只剩下开发商和炒房人了。那时候，炒房人就惨了，为什么？因为有价无市。最不缺的就是房子。只能降价卖。这时，部分自住人继续接盆。炒房人如果是吃了亏，退出。那基本只剩下开发商了。<br />
　　<br />
　　不过开发商不会倒，因为他们和银行勾结，没有人买开发商就把房子给银行来拿钱。<br />
　　<br />
　　 银行会不会变化呢？这是最关键的部分。银行是听政府的，如果政府放任不管，银行会继续和开发商合作。房价继续屹立不动。如果政府迫于通货膨胀等什么压力，要银行停止房贷，减少比例。那开发商最后一根稻草就没了。<br />
　　不过，如果那样，开发商很可能就停供之前那些卖给银行的房子，一大堆坏帐就产生，一大堆烂尾楼就出现。<br />
　　<br />
　　所以说，开发商绑架了政府，就是这个道理。<br />
　　<br />
　　那是不是只要银行不变，这个游戏就可以永远玩下去呢？<br />
　　不一定，呵呵，因为还没有什么国家玩这个游戏玩几十年的。<br />
　　另外一个伴随的东西是：<br />
　　这个过程中，房子，会越来越多，房子不是荔枝，放几天就烂掉，房子能放几十年（呵呵，中国的只有30年），这些房子拿来干什么呢？如果没有人买，那能怎么办呢？只有出租了。。<br />
　　<br />
　　不过，租金就不是象房价一样，有银行来后盾的，租金和房子的成本无关，租金由真正的住户来决定。<br />
　　<br />
　　这才是真正真实的东西！ 举例：之前新闻说的那个投资了好多亿的鬼城，里面有个房子标价100万，500元租给您，您要不要？如果租两套400元一套，要不要？租10套的话100元一套，要不要？为什么没有人要呢？因为没有这个实际需要！但是房的主人租不租呢？50元他也租，因为你不租连1元都没有。一个地方，真正的居住需要多，它的房子租金就高，一个地方真正居住需要少，它1000万的房子也租不了什么钱。<br />
　　房子越来越多，但是在里面生活的人基本不变，那租金会怎么样？<br />
　　<br />
　　所以，我们根本不需要政府压制房价，政府也不需要急着压制房价。就让开发商玩呗，他们越疯狂，真正建的房子就越多，等游戏结束时候留下的房子就越多。难道游戏结束时候他们把自己的房子炸了不成？难道结束时候，他们就让房子永远空置不成？<br />
　　<br />
　　靠政府建房子，很多腐败。靠开发商建房子，他们多少还挺有效率。所以，就让他们玩呗。<br />
　　<br />
　　只是，那些真正自住人，在这游戏的高点把自己搭进去，值吗？<br />
　　<br />
　　房子是什么，不就是砖头瓦片吗？值吗？<br />
　　那些高楼大厦，只会越来越多，而且最终是要住人的。明白吗？<br />
　　<br />
　　当然，如果您觉得200万买的东西10年后20万也无所谓，那您就去玩吧，可能还能涨到250呢。不过，如果您没有卖出去，永远就只是那堆砖头瓦片。<br />
　　基本说完，如果您有耐心看一次，估计您多少都知道该怎么办了。。。<br />
　　<br />
　　10年4月，现在基本就是这个游戏的后部分了，因为真正自住人已经没有多少购买力了。。。接下去，就是开发商和银行玩了。。。。。。<br />
　　<br />
　　呵呵 继续玩吧。。。<br />
　　<br />
