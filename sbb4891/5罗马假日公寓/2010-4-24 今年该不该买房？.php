																											<div id="postmessage_581876" class="t_msgfont">　　（原创）10年4月，用最通俗易懂的方式说明中国房价本质，立贴为证。<br />
　　<br />
　　数据都是虚构的，用来当个例子。尽量说得通俗易懂些：<br />
　　理性的市场是：<br />
　　开发商有100套房子，成本价15万一套，开始市场价格20万一套，买者寥寥。<br />
　　<br />
　　然后，开发商和银行商量，把价格订到40万一套，找马甲自己买下50套，搞按贴（按八成按贴算）。<br />
　　结果：开发商每套能得到40*0.8＝32万的贷款（利息大概是年6％，黑市上估计要60％），8万首付是自己给自己，<br />
　　银行：顺利放贷了32万，当然其中还有一些灰色收入。<br />
　　银行和开发商都是受益人。<br />
　　谁是受害人？表面上是银行，因为它用32万买了原来只需要给16万的东西，但是银行是国有银行，它不需要负责，亏了也是全体人民埋单。<br />
　　<br />
　　然后，开发商还有50套新房子，怎么办？<br />
　　1 如果他想继续贷款，就继续找马甲卖给银行。但是贷款要还利息的。<br />
　　2 他如果不需要贷款，他可以卖，这时候他有材料说了：我的房子已经卖了一半了，40万也有人要啊！ 真正买房住的人可能被说服。40万买一些。开发商得到真正的钱（不需要利息的钱）<br />
　　3 开发商之前的50套自己买的房子呢？如果形势好，可以45万卖出，如果不好，可以35万卖出，只要不低于20万，都是赚钱的，也成功把之前要还的贷款转移到 真正买房住的人身上。<br />
　　<br />
　　4 如果卖完。<br />
　　开发商20万的房子卖出了40万，不需要还贷款。<br />
　　银行：没损失。<br />
　　真正买房住的人：用40万买了20万的东西，还贷款。<br />
　　如果卖不完：<br />
　　开发商只需要继续给6％的利息，拿着这些贷款去干别的。<br />
　　房子空置。<br />
　　银行：没损失。如果开发商的马甲断供，坏帐国家负责。<br />
