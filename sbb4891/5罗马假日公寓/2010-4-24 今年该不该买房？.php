　　<br />
　　分析压抑房价的措施：<br />
　　1 开发商不可以捂盘：基本没用。开发商总是先把好的50套用自己的马甲买了，或者批量卖给炒房人。<br />
　　2 压制地价：基本没用。您把地白给开发商，开发商也只会根据市场定价。（所以，地方政府才没有这么傻，拼命卖地，不赚白不赚）<br />
　　3 银行加息: 有一些用。但是很局限。即使加到10％，这贷款利息还是比黑市低很多，开发商还是可以通过马甲来套钱。不过加息对炒房人和真正自住人打击很大。这间接增加开发商的贷款转移效率。不过，开发商即使转移不了，他也能通过房子获得低息贷款。<br />
　　4 降低按贴比例：这是最根本的一招。一针见血，立竿见影。<br />
　　 开发商为什么这么猖狂，因为即使天下人都不买房，开发商还是能用马甲把房贷给银行来换钱。<br />
