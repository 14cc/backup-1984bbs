　　然后开发商再建100套房子，把每套40万定价为80万。<br />
　　然后，开发商和银行商量，把价格订到80万一套，找马甲自己买下50套，搞按贴（按八成按贴算）。<br />
　　结果：开发商每套能得到80*0.8＝64万的贷款（利息大概是年6％，黑市上估计要60％），首付是自己给自己。<br />
