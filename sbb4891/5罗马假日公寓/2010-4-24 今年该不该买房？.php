　　<br />
　　小结：<br />
　　1 房子的最终价值是它的建筑成本。<br />
　　2 不管房子标什么价格，20万还是40万还是80万，房子还是那个房子，由劣质砖头水泥构成。<br />
　　3 房子没有卖出去前，它的价格只是个数字。<br />
　　<br />
　　评论一些楼市的言论：<br />
　　1 中国缺地？<br />
　　呵呵，其实太平洋缺水。。。。<br />
　　2 地价导致房价高？<br />
　　地价和房价没关系，开发商即使是白拿地，他也会卖这么贵，开发商即使拿地100万一平方，他也卖不出101万一平方。<br />
　　3 中国人多？<br />
　　总量是多，但是密度并不是很高。<br />
　　4 丈母娘效益，大学生效益？<br />
　　得了吧，就中国人有丈母娘。。。<br />
