　　1 如果他想继续贷款，就继续找马甲卖给银行。但是贷款要还利息的，不过利息只是黑市的十分之一。<br />
　　2 他如果不需要贷款，他可以卖，这时候他有材料说了：我的房子已经卖了一半了，80万也有人要啊！ 真正买房住的人可能被说服。80万买一些。开发商得到真正的钱（不需要利息的钱）<br />
　　3 开发商之前的50套自己买的房子呢？如果形势好，可以85万卖出，如果不好，可以75万卖出，只要不低于20万，都是赚钱的，也成功把之前要还的贷款转移到 真正买房住的人身上。如果还有上一批40万的，在这种价位下 一般也能顺利卖出。<br />
　　4，其他人，看到房子从20万涨到80万，产生：房子会越来越贵，房子能升值想法。跟风买入。这时，产生了专业的炒房人。炒房人在以后会基本取代开发商马甲的角色，炒房人会先买之前提到的最早50套，开发商也会乐于买给专业的炒房人，因为炒房人替他们承担了风险和利息和买卖房子的手续费。<br />
