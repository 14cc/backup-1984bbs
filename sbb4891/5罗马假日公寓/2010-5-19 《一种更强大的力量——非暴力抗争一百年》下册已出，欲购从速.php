													<h2>新演进就是不合作的一种方式</h2>
						
						
																											<div id="postmessage_625862" class="t_msgfont">刘荻：演进，在改良与革命之间――我看《零八宪章》<br />
作者：刘荻 文章来源：民主中国 点击数： 更新时间：4/20/2009 7:59:03 AM<br />
<br />
不久前，本人读到与本人同为《08宪章》发起人的余杰先生在其大作中提到&quot;《08宪章》是改良主义的&quot;这一观点。本人对此有不同意见，故撰本文与余杰先生商榷。<br />
<br />
余杰先生认为，《08宪章》的主张是在承认现行宪法的前提下要求修宪。固然，《08宪章》中使用了&quot;修宪&quot;的字眼，但《08宪章》的主张究竟是修宪还是制 宪，在签署者中实际上是存在争议的。有些人认为《08宪章》名为修宪，但提出的十九条主张实际上是对现行宪法的根本否定，是制宪而不是修宪。《08宪章》 签署者中，主张制宪者也大有人在，本人就是其中之一。因此以&quot;修宪&quot;为由说《08宪章》是改良主义，恐怕许多签署者不会同意。<br />
<br />
余杰先生在文中还提到，《08宪章》排除了暴力革命的选择，对此我并没有反对意见；但是排除暴力革命是否就一定意味着改良，对此我有不同看法。<br />
<br />
我认为，《08宪章》的主张既不是革命，也不是改良。那我们的主张是什么？我请大家回到五年前由崔卫平教授主持翻译的米奇尼克文集《通往公民社会》：我们的主张是&quot;新演进&quot;。<br />
<br />
&quot;演进&quot;与&quot;改良&quot;有什么区别呢？&quot;改良&quot;是眼睛向上，把目光放在当权者身上，希望当权者进行改革；而&quot;演进&quot;是眼睛向下，把目光对准社会和公众，我们是 要告诉公众如何行动，而不是告诉政府如何改革，我们的目的是为了凝聚公民的共识，促进社会的觉醒、发育和团结，而不是为了&quot;上书劝进&quot;，乞求政府接受我们 的改革方案。<br />
<br />
&quot;演进&quot;的主张中包含哪些具体行动呢？例如在去年的汶川大地震中，牛博网友不通过政府和官方的红十字会，自行募捐数百万元并亲自购买救灾物资送往灾区。汶 川大地震中参与救灾的民间组织有上百之多，多家民间组织还共同组建了成都5.12民间救助服务中心，以便协调民间救援工作。地震发生之后，大批媒体记者纷 纷自行前往灾区进行报导并参与救灾工作，部分记者从灾区返回后还在北京传知行社会经济研究所举行了面对网友和民间人士的新闻发布会。地震发生十个月之后， 艺术家艾未未开始在自己的博客上公布自己和自己的同事们亲自调查出来的地震遇难学生的姓名、年龄、学校、班级。这些行动都是&quot;演进&quot;的一部分。<br />
<br />
类似的民间行动还有许多。北京网友就曾经组织过多起以公民身份成功营救被非法关押在黑监狱中的上访者的行动。为防止对方使用暴力，每次行动前都会事先召集 围观群众和&quot;打酱油&quot;者。网友们以公民身份与&quot;黑监狱&quot;交涉，要求释放被非法关押的访民。如果对方使用暴力，网友们就通过自己&quot;人多势众&quot;，用拉架的方式 阻止对方使用暴力。通过这种方法，网友们成功营救了多名被非法关押的访民。<br />
<br />
北京的传知行社会经济研究所就是一个致力于从事公民行动的NGO。他们每周六下午举办的公民转型论坛不仅邀请各领域的专家学者来就各种理论问题发表演讲， 也邀请从事实际工作的律师、记者、维权者等来就大家关心的各种社会问题发表演讲，如前面提到的汶川大地震新闻发布会，以及维权律师就毒奶粉事件发表的演讲 等等。除了讲座之外，传知行还制作《2008公民税权手册》等小册子，并且经营了一个立人乡村图书馆项目。从事类似活动的NGO还有许多。<br />
<br />
这就是&quot;演进&quot;：我们既不主张暴力革命，也不企求当权者的改良，我们要组织起来，用自己的力量和平地推动中国社会的进步。我认为这才是《08宪章》的主张。<br />
<br />
2003年以来，由异议人士和广大网友组织的网络联名活动每年都有几起甚至几十起，《08宪章》有什么特别之处，值得政府如此大动干戈呢？我认为，《08 宪章》的意义就在于其发起人和签署者有效地突破了原有的异议人士和维权人士的圈子。《08宪章》的发起人和签署者中有各种职业各个社会阶层的人，其中不乏 所谓的&quot;新社会阶层&quot;，甚至还包括了中共党员、军人和公务员等被认为再&quot;正统&quot;不过的人。《08宪章》已经不再是&quot; 一小撮&quot;人的事了，用某些人的话来说，我们已经有了&quot;群众基础&quot;和&quot;社会基础&quot;。<br />
<br />
如果说《08宪章》的主张是在改良与革命之间，那么《08宪章》的&quot;群众基础&quot;就可以说是在权贵与民粹之间。我们既不寄希望于当权者对政治体制改革突发奇 想，也不像另一些政治力量那样把底层的苦难当作自己的大旗。固然，底层的苦难是值得同情的，也是需要我们关心和帮助的，但仅有这些是不够的。除了社会中最 底层和最边缘化的人们之外，我们还需要得到社会主流支持。学者、法律工作者、媒体人士、企业家、专业人士、白领、&quot; 新社会阶层&quot;……他们具有社会组织能力和动员能力，是社会的&quot;中坚力量&quot;。上文中提到的公民行动，许多都是由他们完成的。没有他们的支持和帮助，底层的运 动很难成功。一场运动如果仅仅诉诸于底层的苦难，虽然能够得到同情，却很难得到社会中更多人的支持。社会的中间阶层本来是社会的稳定力量，只有当他们也对 现状感到不满，希望变革的时候，我们的运动才能真正取得成功。《08宪章》在动员更多主流人士的参与方面是成功的。当然，这并不是说我们应该无视底层的苦 难，我们所要做的是动员中间阶层，帮助底层，只有这样我们才有成功的希望。<br />
<br />
此外，本人还注意到有论者谈到海外民运的&quot;群众基础&quot;问题。本人想请该论者注意：海外民运的目的是要在中国实现民主，而不是在海外竞选公职，因此海外民运 的&quot;群众基础&quot;也应该在中国国内；海外人士可以成为中国民运的促进者和拉拉队，但是民运到海外去寻找什么&quot;群众基础&quot;，无异于南辕北辙。</div>

							
							
							
							
