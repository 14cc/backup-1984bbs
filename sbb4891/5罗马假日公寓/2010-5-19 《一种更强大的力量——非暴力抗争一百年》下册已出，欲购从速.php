													<h2>《一种更强大的力量――非暴力抗争一百年》下册已出，欲购从速</h2>
						
						
																											<div id="postmessage_624028" class="t_msgfont">非暴力不合作运动的经验教训（刘荻）<br />
2010-05-18<br />
<br />
彼得&#8226;阿克曼与杰克&#8226;杜瓦尔合著的《一种更强大的力量》一书中文版的上册即将问世。这本书回顾了20世纪非暴力斗争的历史，并总结了其中的经验教训。作为译者之一，本人初步整理了书中所总结的非暴力不合作运动的经验教训，希望能够为我们今后的行动提供指导。<br />
<br />
一、谈判和妥协<br />
<br />
在本书列举的所有案例中，谈判和妥协都很重要。最突出的例子可能就是波兰的团结工会了。1980年8月，波兰格但斯克地区的工人用总罢工把政府逼到了谈判桌上，迫使政府同意工人成立独立工会的要求，条件是工会不得参与政治活动，工人必须拥护波兰统一工人党的领导，不得反对波兰与苏联的结盟关系。共产党国家中唯一能够合法活动的独立工会――团结工会就此成立。在接下去的十六个月里，团结工会通过不断威胁罢工然后与政府谈判并达成妥协，成功迫使政府允许团结工会使用官方电台和电视台，以及出版自己的报纸。短短几个月时间里，团结工会会员就发展到上千万人――而与此同时，执政的波兰统一工人党党员人数只有300万。农民和大学生也纷纷效仿团结工会成立自己的组织。虽然团结工会只存在了十六个月就遭到镇压，但它所取得的成就在社会主义国家中是空前的，而且它还为若干年后波兰的政治转型奠定了基础。<br />
<br />
这方面的反面教材是1923年的鲁尔抗争。第一次世界大战之后法国和比利时为迫使德国支付战争赔款，派军队占领德国的鲁尔地区，企图夺取鲁尔的煤和焦碳等重要物资。鲁尔地区的德国人遵照本国政府的命令“消极抵抗”。一开始，德国工人的罢工成功阻止了法国人在鲁尔挖煤，但德国政府却没有在抵抗达到最高峰时及时与法国人谈判达成妥协。后来法国人运来了本国和其他国家的工人代替德国工人的工作，法国人的镇压和节节攀升的通货膨胀也使抵抗陷入低潮。这时德国政府想要谈判，法国却拒绝了，最后德国只好认输。<br />
<br />
要想成功地通过谈判和妥协来实现自己的目的，我们就必须注意几点：<br />
<br />
1.接受对方的让步。1905年，俄国工人通过总罢工迫使沙皇尼古拉二世发表《十月宣言》，承诺公民自由和宪政改革。但反对派中的布尔什维克和孟什维克等激进分子却不以此为满足，企图发动暴力革命，结果遭到了残酷镇压。本书作者写道：“托洛茨基诋毁《十月宣言》，说它‘什么也不是’，并且要求建立人民共和国时，他实际上是对运动已经实现的成果视而不见，并且让获得立即满足的欲望成为最终胜利的敌人。”“如果有更多反对政权的人士把《十月宣言》当作一项突破――承认人民拥有权力和与生俱来的权利――而不是一套令人鄙夷的权宜之计，那么宫廷内支持改革的人士就有可能说服沙皇镇压是不必要的。但当时激进派过于狂热，右派的暴力和左派的过于自信破坏了这一良机。”通过民众运动来改变一个政权往往是一个较为漫长的过程，很少可能通过一场运动就取得最后的胜利，因此我们就必须接受对手做出的较小让步，而不是一律将其视为敌人的诡计而加以拒绝，让“敌人意识”成为取得最终胜利的敌人。<br />
<br />
2.参加对方的游戏。1988年10月，智利军事独裁者皮诺切特为了给自己继续执政制造合法性而举行全民公决。反对派广泛动员，最终在全民公决中击败了皮诺切特。<br />
<br />
3.最重要的一点是：非暴力运动的目的是迫使政府妥协，而不是充当武装起义的跳板。本书作者写道：“当非暴力运动试图将暴力纳入其战略时，它获得成功的可能性就会降低。一旦一个政权受到致命力量的攻击，它聚合内部的支持并展开镇压的能力就会提升。”在1905年的俄国、1923年的鲁尔和20世纪80年代的智利，主张暴力的激进派都曾给非暴力抗争造成伤害。<br />
<br />
二、利用国际国内环境中的有利因素<br />
<br />
1905年的俄国工人利用了俄国在日俄战争中的失利。反抗军事独裁的阿根廷人利用了英阿马岛战争的失利。争取独立的印度民族主义者利用了第二次世界大战之后英国国力空虚。波兰工人利用了经济危机。书中讲述的多数非暴力运动都在不同程度上利用了国际社会――尤其是英美的压力以及统治集团内部的分裂。外国独裁者的倒台也是非暴力运动可以利用的因素。<br />
<br />
三、大众参与<br />
<br />
与主要依赖密谋和策反武装力量等手段的暴力革命不同，非暴力运动广泛依赖宣传、动员和大众参与。第二次世界大战期间抵抗德国占领的丹麦抵抗运动成员弗洛德&#8226;加科布森（Frode Jakobsen）指出：“对我而言，争取我们民众的灵魂的斗争是最为紧要的……对我来说，问题必然是：‘一个人怎么才能让大量的民众参与到战斗中来？’ 而非：‘一个人怎么才能最大限度地伤害德国人？’我敢打赌，如果效果是一样的，让1,000人参与到行动中来比让10人参与更好。”这是对非暴力运动的大众参与本质的最好描述。<br />
<br />
暴力活动会对非暴力运动造成伤害，这不仅是因为暴力可能招致镇压，还因为少数激进分子的暴力活动会吓跑支持者，尤其是本来可能参加运动的中产阶级和主流人士。暴力行动难以赢得普通民众的同情，更不用说参与了。如果参加运动的只有少数极端分子，而中产阶级和普通民众都留在家中，那运动就失败了。<br />
<br />
四、建设性<br />
<br />
非暴力运动不仅是为了推翻一个旧世界，更是为了建设一个新世界，后者甚至更加重要。与前者相比，后者的任务更加繁重，也更加艰巨。推翻旧世界只是一个单一目标的任务，而建设新世界则是多种目标并存。自由、民主、法治、宪政、人权、公民社会……都是我们努力的目标。建设性的工作更加依赖广大民众的参与，对国家的未来也更加有意义。萨尔瓦多反对派集合了社会各阶层人士――包括前政府官员和军方人士的支持，成功推翻了独裁者马丁内斯将军。但是由于没有一个建设性目标，推翻独裁者并没有给该国带来民主，萨尔瓦多不久之后就再次陷入一种新型的军事独裁。与之相反的是，印度由圣雄甘地领导的“建设性工作”和“自治运动”，以及波兰反对派的“建设社会”工作，都为在取得胜利之后建设一个新的民主国家奠定了基础。<br />
