公告二：第二届偶发艺术节紧急通知 <br />
6月1号中国第二届现象-偶发艺术节由原来的地点：（户外）宋庄和燕郊潮白河桥往北（运河苑度假村）前行300米零点四厘米更改为：宋庄当代美术馆后院（草坪树林环绕）！！&nbsp;&nbsp;望各位参与者相互转告，其他场地不变！<br />
