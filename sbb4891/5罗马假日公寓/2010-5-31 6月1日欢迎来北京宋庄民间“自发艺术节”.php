公告二：第二届偶发艺术节紧急通知 <br />
6月1号中国第二届现象-偶发艺术节由原来的地点：（户外）宋庄和燕郊潮白河桥往北（运河苑度假村）前行300米零点四厘米更改为：宋庄当代美术馆后院（草坪树林环绕）！！&nbsp;&nbsp;望各位参与者相互转告，其他场地不变！<br />
<br />
2010年第二届偶发艺术节”邀请函<br />
<br />
<br />
致各路自由艺术豪杰：<br />
<br />
2009年，第一届“偶发艺术节”，在众多老牌艺术家和年轻艺术家的积极支持、关注和参与下得以圆满成功，并深受好评。因此，2010年6月，第二届“偶发艺术节”，又将开始啦。<br />
这个艺术节的特点就是，不太正经、不太听话、不太顺从。很多艺术家喜欢它，就是因为它有一个自由的话语舞台和完全开放的表现空间。<br />
<br />
“偶发艺术节”，是纯粹意义上的中国民间当代艺术家的自发行为，是坚定的民间当代艺术立场和态度，是打破伪学术泛滥争取自由话语的当代艺术野史见证。是颠覆自我、颠覆伪艺术 ，建构新文化表达的独立艺术精神。<br />
<br />
艺术不是请客吃饭<br />
<br />
每个时代的年轻人都会做出影响世界的事情，从50年代的“垮掉一代”，60年代的“嬉皮运动”，70年代的诗歌运动，80年 代的当代艺术思潮，再到90年代的圆明园画家村及东村艺术等 ，其影响直到今天。<br />
<br />
现在，2010年6月第二届“偶发艺术节”，又将是一个属于时 代年轻人的舞台。今年的主题是“点燃艺术的野火”，这是一 股充满激情的力量，这力量将以集体释放良知的姿态告诉世人，今天的艺术不是请客吃饭，不是做文章，不是绘画绣花，不 能那样雅致，那样从容不迫，文质彬彬，那样温良恭俭让。今天的艺术是对消极和所有不道德的彻底颠覆。<br />
<br />
6月里的年轻人，携带燃烧的青春，与这个日益平庸的社会现实针锋相对的时刻即将来临。<br />
<br />
活动内容：音乐现场、行为艺术、自由表演、影像艺术、绘画艺术、图片艺术、诗歌现场。<br />
