																											<div id="postmessage_737788" class="t_msgfont">孔子说：“人而无信，不知其可。”我想这两天对这句话最有戚戚感的应当是唐骏。方舟子发现他文凭造假，他却还要出来为自己辩护，说实话，我觉得他实在不算聪明。譬如他为了撇清与加州理工大学的关系，就供出了一个所谓西太平洋大学，结果不但又让人揭露出这所大学的野鸡身份，还连累他很多校友纷纷出来忙着改学历，害人害己，丑闻便越滚越大。如果当初他能够勇敢一些，出来声泪俱下作出一点忏悔的样子，我估计还可能获得一些同情。<br />
&nbsp; &nbsp; 其实我对唐骏造假事件的态度颇有点纠结，据说有一些挺唐者，认为英雄莫问出处，这话也不能说毫无道理。例如当年刘备说他是汉室宗亲，是中山靖王刘胜之后，我敢百分之百肯定他是在造假履历，尽管后来汉献帝亲口认了他做皇叔，但那是因为在曹操欺压之下要寻靠山，所以说到底还是利益交换，正如你给我两千块，我给你一张文凭一样。而如果没有这一个假履历，我看刘备这一辈子顶多也就是个草鞋批发商。<br />
&nbsp; &nbsp; 又有一些挺唐者很气愤地说：“有那么多作官的造假学历，还有那么多教授抄袭论文，为什么非要揪住一个商人的假文凭不放呢？”这话当然放不上台盘，唐骏既然被抓出来了，那么他总是要付出代价，比如我不相信他，就不会再买他的股票，据说他的东家也打算要辞退他。但是，如果守着诚信就做不成事，而要想做成事又守不住诚信，在这样的情形下，唐骏的假文凭也多少有点逼上梁山的意思。而在一个缺乏诚信的社会里，倒下一个唐骏，却丝毫不妨碍那些李骏、王骏们继续拿着假文凭走向成功。<br />
&nbsp; &nbsp; 我说这些决不是要为唐骏辩护，我只是觉得在我们纷纷指责唐骏人品的时候，还应当考虑那些情势使然的社会原因。老实说，当唐骏暴出学历造假丑闻时，我一点都不感到惊讶，相反，正如刘永好曾经说过中国的富豪都有原罪一样，像他这样一个“成功人士”如果从没有过假言假行，我倒会觉得奇怪了。我们不妨想想，在我们的社会里，有几个诚实的人能够赢得成功和尊重？又有几个被人追捧的成功者是诚实的呢？换句话说，当我们都不再尊重诚信者的时候，我们又怎能够理直气壮地去指责不诚信的人呢？<br />
&nbsp; &nbsp; 所以，从社会原因这个角度，我以为我们与其在唐骏身上炫耀自己的道德感，还不如认真想想在我们的社会里还有多少道德？而作为这个社会一分子的我们又是否还能够真正地坚守道德？如果不能直面这个问题，那我们对这个不诚信的社会终究也要承担或多或少的一份责任。譬如在平常生活中，我们为了提职称、涨工资，为了升官，为了争取多一些的实惠，说过多少违心的假话，又做过多少在德行上有亏的事情？如果我们早就习惯了这些，又何以对唐骏的假文凭如此大惊小怪？<br />
&nbsp; &nbsp; 发誓要把打假坚持下去的方舟子是值得敬佩的。但是，如果他的努力只是为我们提供了一些挥舞道德大棒的对象，而不能促使我们对社会、对自己做更多的反思，那他最终也不过是一个堂吉诃德式的人物。</div>

							
							
							
							
