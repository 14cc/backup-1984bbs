													<h2>曹长青：异议人士怀念布什总统</h2>
						
						
																											<div id="postmessage_616624" class="t_msgfont">布什总统才卸职一年多，历史如何评价还需要时间。但很多专制国家的异议人士却已经开始怀念布什了。四月下旬在美国南方卫理公会大学（SMU）召开的“怎样用网络突破专制”的全球异议人士会议上，就有多位异议人士表达了这种看法。 <br />
<br />
《华尔街日报》社论版助理编辑芭莉-韦斯（Bari Weiss）最近在题为“布什之后的推广自由项目”的报道中引述说，来参加这个会议的叙利亚异议人士亨迪（Ahed AL-Hendi），2006年的时候，正在大马士革的一家网吧浏览民主网站，突然便衣警察拿着自动冲锋枪涌进来，把他带上手铐，扔进汽车尾部的车厢。随后在一个月的时间里，他被关在一个五英尺长、三英尺宽、没有窗户的地牢中。他听到朋友在隔壁房间被拷打时的痛苦喊叫。他说，这种喊叫，是“冰冷的安慰”，因为至少告诉他，朋友还没被杀死。 <br />
<br />
韦斯的报道说，亨迪是幸运的一位，现在他作为政治难民，住在美国的马里兰州，在“异议人士网络” （Cyberdissidents.org）的组织工作。在这次会议上，亨迪见到了前总统布什。布什总统夫妇还请他一起单独午餐。亨迪代替很多异议人士对布什说，“我们想念你！”他说，奥巴马当选之后，美国出现“很多变化”，但却不是变得更好。 <br />
<br />
亨迪说，布什政府和奥巴马政府，对他的母国叙利亚的政策明显不同。“在布什总统时，只要有任何异议人士被叙利亚政府逮捕，白宫发言人都会谴责。但现在奥巴马政府却根本不吱声。”奥巴马政府反而对严厉镇压异议人士的叙利亚当局实行“交往政策”（engage），最近决定向大马士革派驻美国大使，这是自从 2005年以来的第一次。亨迪说，“这等于增加叙利亚政权的自信。但大马士革当局没有对话的能力，他们不相信对话，他们只相信武力。” <br />
<br />
参加这个大会的北朝鲜异议人士洪阿典（Adrian Hong），在2006年时，曾在中国被关押，因为他帮助北韩难民逃亡。他说，“我当时被关了十天就放了，后来得知，是因为布什政府强烈关注这个事件。当然，也是因为布什总统推广自由的政策使然。”洪阿典感叹，当年布什总统多次公开、高调地接见北朝鲜的异议人士们，“向平壤及世界展示，美国把人权和自由作为政策优先。”但现在的奥巴马政府，却把这种政策完全放弃。 <br />
<br />
来参加这个大会的委内瑞拉异议人士格拉尼尔（Marcel Granier），是委内瑞拉历史最长、收视率最高的电视台RCTV的总裁，他雇用了几千人。但在2007年，委内瑞拉的强人查韦斯吊销了他的电视台的执照。他后来致力把电视台变成有线台（cable），但今年一月份查韦斯下令，全国有线电视网不可以给电视频道。查韦斯的支持者们，两次用催泪弹，袭击他的住宅。但他仍坚守在他的国家，倔强地坚持致力推广新闻自由。 <br />
<br />
韦斯的报道说，像很多参加这次全球会议的其他异议人士一样，格拉尼尔在奥巴马刚当选总统时，也是非常激动，认为这是一次创造历史的选举。但是奥巴马进入白宫这一年半来，委内瑞拉的查韦斯大步走向独裁专制，而奥巴马政府却什么也不做，对此保持沉默。他幽默地说，“在阿富汗，至少阿富汗人知道，美国至少今后十八个月，会管那里的事情。” <br />
<br />
韦斯的报道说，异议人士的这种被抛弃的感觉，被奥巴马政府的政策变化更加刺激。最近，奥巴马政府决定将把布什政府时确定的推动埃及民主化的资金砍去一半！支持约旦、伊朗民主的资金，也将削减。奥巴马政府引人注目的姿态是：奥巴马会见达赖喇嘛之后，让达赖喇嘛从后门离开（不让媒体拍到，怕惹怒中共），对伊朗示威者的支持少到可怜，但奥巴马却去跟委内瑞拉的独裁者查韦斯握手、微笑。等等。 <br />
<br />
韦斯的报道没有提到是否有中国异议人士参加这个大会。但布什总统曾经两次公开接见中国异议人士、维权人士和基督徒，传递对中国宗教自由、民主化的支持。但奥巴马上台一年半以来，从没有对中国民主运动有过任何公开的支持；甚至在访问中国，在上海给大学生演讲时，只字不提“人权”两字。布什总统访问北京时，特意去教堂做礼拜，表达支持宗教自由。而奥巴马访问北京，则是去逛故宫，登长城，拍了十几张长城姿态照放到了网上。 <br />
<br />
所以，不仅世界其他国家的异议人士想念布什，致力民主化的中国人，也在怀念这位把推广自由价值作为美国对外政策核心的领袖。布什在访问波兰时誓言：“不再有雅尔塔”！即不再有对邪恶的妥协，不再有对推广民主价值的回避，承担起美国作为自由世界旗帜的道德责任。在全球异议人士大会上，人们怀念这样的美国，这样的自由世界领袖！</div>

							
							
							
							
