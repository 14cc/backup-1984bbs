													<h2>与茅于轼商榷   要客观承认 强奸有它的好处</h2>
						
						
																											<div id="postmessage_371314" class="t_msgfont">茅于轼说，要客观承认，专制有他的好处。我坚决拥护。同时为了进一步表明立场，我论证一下：要客观承认，强奸也有它的好处。<br />
<br />
　今天，在这一新时代的当口，我最为关心的是，以后的三十年或者更长的时间里，中国的强奸现象究竟会朝哪个方向发展？我认为这个问题，兼具理论和现实双重意义。<br />
<br />
首先说，生孩子是一种很成熟的社会运行制度，放眼全球，没有一个国家拒绝生孩子的制度。所以，生孩子是普世的价值。但是，在资本主义国家，由于个人欲望泛滥和责任感消失的双重挤压，出现了出生率下降，人口负增长，男女性交无法带来孕育孩子的结果，造成了严重的社会后果，长期下去不堪设想，但是，由于资本主义民主制度的固有缺陷，无法强迫人民不戴安全套进行性交，所以，他们对此束手无策。<br />
<br />
同样，在我国也存在这种不以生孩子为目的的性交行为。由于科技进步，像安全套毓婷等产品帮助人们实现了这样的目的。根据我的论证，生孩子是普世价值，所以，孩子越多，社会发展越快，而我国现阶段采取的是强奸合法化制度，这样一种制度当然制造了一些诸如自杀、打斗等后果，但是，客观地讲，强奸也有它的好处，那就是有利于生孩子。因为谁在强奸的时候也不会准备安全套的。<br />
<br />
像美国德国等国家，我去过，他们的制度是强奸非法化，所以人口越来越少，社会钱途一塌糊涂。<br />
有些人说，强奸不好。确实，现在看来，强奸的弊端日渐突出，但大家也要客观地强奸有它的好处。我想，大家也不用拿“强迫、野蛮”来替代“强奸”这一说法，因为，我们宪法上明确写的，就是无产阶级优先强奸。我曾先后几次提议，要将强奸这一提法改一下，但一直没改。看起来，强奸还是要继续下去。<br />
<br />
