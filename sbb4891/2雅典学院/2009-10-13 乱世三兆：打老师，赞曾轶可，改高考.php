																											<div id="postmessage_280599" class="t_msgfont">乱世三兆：打老师，赞曾轶可，改高考<br />
刘松萝<br />
<br />
据报道，12省市高考新方案已经推出，高考改革渐次展开。对于高考改革，很多人发表过不同的意见，提醒仓促的改革有可能导致腐败和混乱。现在，不听劝告，不计后果，贸然推行这种不靠谱的改革，可谓一意孤行。<br />
<br />
该说的已经说过了。现在，仅仅讨论高考改革已经不够了。高考改革，仅仅是社会的乱象之一。种种乱象，甚至可能成为乱世的预兆。<br />
<br />
乱世预兆之一，打老师。<br />
<br />
一位学生在学校尿了裤子。这里面有老师的问题，比如说拖堂，无端惩罚学生，态度不好等等。家长去投诉，要求处分老师是合理的。这位家长作为男人，竟然去打女教师。打了还不算，还要到论坛上去炫耀。更应该引起我们重视的，是网上一片“该打”的声音。我知道一些学校的情况，也知道老师无端被打的事情是很多的。我想问一问，世界上有哪个国家像中国一样，有那么多的人认为老师该打？这些事情，充分说明了我们正处在一个野蛮的社会之中，说明所谓的中华上邦已经成为彻头彻尾的蛮夷。<br />
<br />
对此，网友addans君写了《一个流氓掌掴女教师之洋洋得意》。我坚持认为，老师打学生是恶，学生打老师是邪恶。<br />
<br />
乱世预兆之二，赞曾轶可<br />
<br />
在快乐女声的竞争之中，没有唱功，甚至可能是五音不全的曾轶可姑娘进入了十强。在下一轮被淘汰以后，还引发了不少观众和网友的失望甚至愤怒。而在比赛的前前后后组织者和评委的表现，用丑行和丑剧来描述，是一点也不过分的。<br />
<br />
有人会说，快乐女声只是一场娱乐活动。这话不假，不过快女在开张的时候，并没有告诉我们其中的规则是娱乐，是儿戏。还有人会说，不要小题大做，娱乐就是娱乐，不会影响到社会。问题在于，现今的中国人缺少范畴的概念。大学的堕落，学术的庸俗化，就是从娱乐和追星开始的。<br />
<br />
这件事情告诉我们，社会上已经没有了客观标准，也没有多少人打算尊重规则。既然竞争的结果与唱的好坏没有关系，那么今后诸如潜规则一类的事情就更好操作了。<br />
<br />
至于曾轶可应该胜出的理由，比如原创，就不用说了。至于个性张扬，绵羊音等等，待过了一年，冷静下来我们就知道有多勉强了。<br />
<br />
《北京晚报》曾有文章指出，有谁喜欢曾轶可的歌，去听好了。有哪个实力人物看好，去给她出唱片好了。规则，是不可以破坏的。<br />
<br />
乱世预兆之三，高考改革<br />
<br />
就在高考改革的消息出来之前，武汉大学发生了弊案。该校常务副校长陈昭方、党委常务副书记龙小乐，因涉嫌在基建工程中巨额受贿被捕。媒体称，此案“震惊”了中国的教育界。没有人怀疑，这只是冰山一角。<br />
<br />
没有人会否认，中国的吏治和廉政正处于最差的时期之一。此时，高考改革方案的制定者们不知道是真天真还是假天真，一定要忽略高考中监督的成分，增加可以随意操作的成分。这样做，除了助长腐败之外，看不出有什么其他的结果。<br />
<br />
关于高考改革，北京语言大学教育测量研究所所长谢小庆先生的话最能够说明问题：“任何进步都需要付出必要的代价。容忍一些腐败，容忍一些贪官污吏、豪门权贵将自己的子女送进大学，是为了发展素质教育不得不付的代价。”专家们，早就没有人相信了。我想，这样改下去，到底是为了素质，还是为了容忍腐败，恐怕很清楚了。<br />
<br />
小结<br />
<br />
我们要警惕：打老师，赞曾轶可和高考改革有可能成为乱世的预兆。人人都不相信规则，不承认规则，这就是乱世的开始。<br />
<br />
就拿考试和比赛来说吧。权势者希望没有制约，而大众则希望通过各种花招来规避规则。曾轶可现象，以及高考时考生用甲骨文来写作文，皆是如此。其中的伎俩，与文革中的交白卷是一样的。<br />
<br />
应该讲制度的时候去讲个性，是非常危险的。<br />
<br />
