<br />
做，只要我们每个公民都只做一点点力所能及的事情，公民运动就会成为浩瀚的的洪流，冲破专制的羁绊，奔向光明的未来。那么它们欲要强加在晓波身上的11年梦魇就会在明天结出灿烂的果实。<br />
<br />
