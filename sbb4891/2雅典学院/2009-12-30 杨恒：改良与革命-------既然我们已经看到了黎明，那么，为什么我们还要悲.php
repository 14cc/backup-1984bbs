<br />
只在口头上讨论革命和改良是没有意义的，实际上，真正的革命和改良是一体的，激烈的改良就是革命，渐进的革命就是改良，这是一体的两面。争论这些是无谓的、耗费精力的，只有那些“口头革命派”和“口头改良派”才会沉溺这种口水战中。其实他们都是坐而论道。没有行动，再激烈的革命理论和口号都是对天瞎扯；天天在行动，再小的改良行动都充满着希望。关键不是谁能说狠话，关键是谁在行动！<br />
