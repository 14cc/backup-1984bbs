																											<div id="postmessage_739879" class="t_msgfont">噗，这种贴儿就把你忽悠啦？<br />
<br />
这么说吧，这篇文章三个问题。<br />
<br />
一，没有注意一个必然性：社会结构的演变中大量产生暴力，中法类似是不得已。<br />
<br />
英国是被诺曼底人征服的，法国人的等级观在与英吉利人融合过程中淡化了，所以英国经常出现王权、贵族、市民三者之间两两结合二对一的局面，而在法国本土，等级森严，贵族是打死不和市民整个阶层联盟的，老是去分化一部分，让其成为官僚，然后死命压榨另一部分（和现在的天朝多么类似）。这样下面一盘散沙没有中间层缓冲的社会结构，共个鬼和。<br />
<br />
二，把理论和程序混为一谈。<br />
<br />
原文“根源就在法国的唯理论本身有先天的缺陷，就是那些的概念都是一些的先天的假定的概念和理想状态中的东西，无论卢梭的自然人和自然社会的概念，民主和自由人权还有契约和儿童，都是一些的假定，是没有社会实践来支撑的东西，因此，其结果必然变形。”这里两个问题：1，这些概念是否比原来的先进（我们不说绝对的正确）？应该说是的。2，“没有社会实践来支持”――这就是废话了，理论刚出现的时候当然全是假设，用什么程序来实现这些理论，英美法各有不同，那是后人的问题。<br />
<br />
“许多人在谈的不是美国的民主自由人权，只是打一个旗号而已，其本质还是在谈法国的民主自由人权，也就是还是理想性的，先天性的，唯理论的。”――美国纲领性质的《独立宣言》谈的还是法国的民主自由人权，没有另开药方，实现的路径有别，方向是一样的。<br />
<br />
三，最后没有观点。<br />
<br />
“现在中国搞民主搞自由人权，也不能靠法国的唯理论的思维方式，而还是要用中国人自己的经验论和实用主义，可以参考英美经验论和实用主义好的方面，来搞中国自己的改革。”一个不能靠，一个只能参考，到底是什么办法，无解，资料一堆，看了一气，白看。</div>

							
							
							
							
