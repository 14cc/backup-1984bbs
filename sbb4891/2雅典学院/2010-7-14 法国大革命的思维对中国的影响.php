													<h2>法国大革命的思维对中国的影响</h2>
						
						
																											<div id="postmessage_738741" class="t_msgfont">法国大革命的思维对中国的影响<br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
近代以来，对中国影响很大的欧美力量之中，影响中国最大的不是英美派，而是欧洲大陆，有德国的影响，也有法国的影响，其中最大的是法国的影响，尤其是法国大革命对中国的影响非常的大，无论是孙中山的国民党的革命还是毛的共产党的革命，都是受到法国大革命的影响，而不是英国和美国的影响。<br />
<br />
<br />
<br />
<br />
而法国大革命的影响，从思维上说，就是欧洲理性主义的影响，与英美的经验论和实用主义是非常不一样的。欧洲的大陆理性思维方式，与英美的经验论为主的思维方式是非常不同的。一般人会认为，中国人的思维方式是经验型的，是归纳型的，因此中国人最可能接受的是英美的经验论或实用主义，但事实上恰恰相反，中国人在近代接受的是德国和法国式的大陆理性主义。<br />
<br />
<br />
<br />
<br />
法国大革命的理论基础是启蒙思想家，主要是孟德斯鸠，伏尔泰和卢梭的思想和力量。这些人，比如说卢梭，卢梭有一些的书影响非常的大，一个是《论人类不平等的起源和基础》，一个是《社会契约论》，还有一个在教育上影响很大的《爱弥儿》。孟德斯鸠有《论法的精神》，孟德斯鸠是三权分立，君主立宪制度的倡导者。卢梭的思想虽然有情感的因素，也有反理性的因素，但那只是相对伏尔泰而言的，他们这些启蒙思想家还是有共同的特色的。<br />
<br />
<br />
<br />
<br />
法国思想家以及卢梭的思想是唯理论的，唯理论的一个特点是，先假定一些先天的概念，然后开始演绎。德国哲学家也有这个特点。卢梭思想的几个假定的概念，一个是自然人的概念，一个是人民主权，还有一个是自然社会和社会契约，在教育上还有一个儿童的概念。这些概念的一个总的特点就是都是一个个的假定，就是在理想化的状态下，民主是什么，自由是什么，人权是什么，法是什么，契约是什么，儿童是什么，多少有些乌托邦的意思，然后，其推演的结果就是，比照这些理想的概念，现实是什么状态，当然现实都是不好的，那么我们应该怎么办呢？我们当然要改变现实，以达到这个理想化了的先天概念给我们描画的美好的图景。<br />
<br />
<br />
<br />
<br />
因此，批判现实，反抗现实，挣脱旧世界，甚至砸烂旧世界，为实现理想而不惜一切，还有激烈的革命热情，都是法国大革命的特点，后来也是中国近代革命的特点。卢梭的问题是，其自然人的概念，人民主权的概念，都是一个不受控制的理想状态的概念，因此，其革命的特点就是无法无天。无论是孙中山的国民革命，还是毛的共产革命，都是法国大革命的效法者和实践者。孙中山的历次革命，反对皇权，不要君主立宪，搞共和革命，都是法国大革命的特点；而共产主义本身就是乌托邦，包括空想社会主义，共产党人的革命热情和革命理想也是非常高的，不惜流血牺牲，也很令人钦佩，但是，无论是孙中山还是毛泽东，他们革命的结果都不是很好，而且往往与他们的理想和初衷并不一致，甚至截然相反，这又是为什么呢？<br />
<br />
<br />
<br />
<br />
根源就在法国的唯理论本身有先天的缺陷，就是那些的概念都是一些的先天的假定的概念和理想状态中的东西，无论卢梭的自然人和自然社会的概念，民主和自由人权还有契约和儿童，都是一些的假定，是没有社会实践来支撑的东西，因此，其结果必然变形。因为，现实是非常复杂的，不同的国家有其不同的历史和现实，与那些理想状态的东西是不是能契合，谁也不知道。<br />
<br />
<br />
<br />
<br />
法国人自身的革命实践就非常的失败。法国大革命最后的结果非常的不好，那些的革命党人，采用了暴力的手段，推翻了皇权，砍了法国国王的头，杀掉了很多的贵族，还摧毁了许多的教堂和教士阶层，最后搞得是乌烟瘴气，一批一批的杀人，人类的私欲和暴力的一面被最大限度的调动起来，其目的是为了达到所谓的民主自由和人权，但其革命的结果却是离开这个理想越来越远，最后不得不用拿破仑来收拾残局，最后也是不得不恢复了王权，拿破仑还称了帝。法国大革命后，其经济和政治环境都大幅恶化，国家一直动荡不安，政体变来变去，从第一共和国到第五共和国，与英国的发展水平相距越来越远。后来这个欧洲强权法国在军事上是一再败于后起之秀普鲁士和德国，在二次大战中三个星期投降。这就是法国大革命的结局。<br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
为了一个理想的东西，不惜动用武力和暴力，为了一个不存在的东西，不惜实行阶级的镇压，杀掉某一个阶层的人，这就是法国的唯理论带来的后果。所以，我们可以说，理想主义是非常可怕的东西，理想是能杀人的。<br />
<br />
<br />
<br />
<br />
中国革命，在近代，开始本来不是革命，是梁启超他们要搞的君主立宪制度，这个制度对于当时的清帝国来说，是改良而不是革命，清帝国的统治者后来在1900年之后也开始了立宪的准备，而且也开始动了，可是，由于满清贵族太贪恋权力，又由于孙中山他们号召革命，而不是改良，结果，一大批的人，在法国大革命的革命激情的鼓舞之下，开始了国民革命，结果是皇帝没有了，中国进入到了共和时代，但是现实的结果当然是天下大乱。共产党表面上是受所谓的马克思理论的影响，受所谓的苏俄革命的影响，但马克思理论的基础是法国启蒙思想，是大陆唯理论的思想，包括德国哲学的影响，在实践上，是受法国大革命的直接影响的。因此，我们可以看到，国民党的革命和共产党的革命何其相似，其革命的语言都是完全相同。所不同的是，国民党的革命还没有革乡绅地主的命，而共产党的革命是革了乡绅地主的命。<br />
<br />
<br />
<br />
<br />
共产党的革命，实现的目标倒不是什么民主自由，人权，契约，而是共产主义，但其运用的手段，完全是法国大革命式的。那么，共产党实现了共产主义的理想了吗？当然没有，因为那是乌托邦，是马克思的唯理论的想象，在中国的历史和现实中，是不可能实现的，无论毛如何将其理论和理想一再的下行，一再的修正，将马克思的理论和中国的具体实际相结合，结果，当然还是失败。最后，只有靠邓小平出来，开始放弃那个先天共产主义的假定，开始走经验论和实用主义，也就是放弃了欧洲大陆的唯理论而开始靠近英美的经验论和实用主义。<br />
<br />
<br />
<br />
<br />
那么，这个影响中国近代历史百年的唯理论现在没有了吗？当然还有，不过是改换了一个马甲，共产主义是不谈了，他们开始谈卢梭孟德斯鸠他们的民主自由人权契约了。当然，有些人不说自己在谈法国的唯理论，而是在谈美国的自由民主人权。可是，美国的自由民主人权与法国的自由民主人权完全是两回事。关于法国和美国在这方面的区别请参阅笔者的《神本与人本》一文。<br />
<br />
<br />
<br />
<br />
只是，笔者闻到的气味，这些人，包括一部分中国的自由知识分子，许多人在谈的不是美国的民主自由人权，只是打一个旗号而已，其本质还是在谈法国的民主自由人权，也就是还是理想性的，先天性的，唯理论的。是一个先天的理想的东西在那里放着，然后大家一起向那个方向努力，其手段当然首先是批判现实，因为现实与那个理想的东西有差别，因此这些人就拼命攻击中国的现实，逢中必反，用一些的理论来套中国，只要不合这些的理想性的概念，那就要开始攻击。他们的一个最大的特点，是没有从中国的现实和历史出发来看问题，而是从一个理想的先天的概念来看问题，这恰恰是法国人的思想和思维，是唯理论的思维，而不是英国和美国人的思维。可是，这些人以为自己是在运用英国和美国人的思维，而且这些人可能还生活在英美，可是他们根本不了解英美，不了解什么是英国的经验论和美国的实用主义。<br />
<br />
<br />
<br />
<br />
英国的经验论在政治上的一个结果是，开创了英国的君主立宪体制。美国的经验论和实用主义是在美国没有发动法国式的大革命，而是搞了美国式的民主体制，是非常适合美国自己的历史和现实与文化的体制，因为，美国人在美国独立战争以前，是有200多年的实际上的民主自治实践的。只要看看那个19世纪法国人写的名著《论美国的民主》，就可以明白法国人是如何看待美国式的民主的，以及法国民主与美国民主的不同的。<br />
<br />
<br />
<br />
<br />
当一个人开始用一些先天的概念来制定一套制度的时候，就是唯理论的思维，然后用这套理想性的制度来套一个非常复杂的现实，并要求现实向这个理想的概念和方向进行改变的时候，就是法国大革命的思想和思维，就是是法国唯理论的思维。<br />
<br />
<br />
<br />
<br />
而英美经验论和实用主义的思维是，不是用一个概念来套现实，并要求现实向这个概念和方向转变，而是从具体的现实，具体的国家的现实和历史文化中，进行缓慢的改良和改革，来慢慢从现实中提出一些可行的方案和操作的方法，甚至制度的创新来实现一个国家和民族的进步。这恰恰是我们要学习英美的地方。而我们很多人是用法国的思维方式来解读英美的做法，岂不是南辕北辙？<br />
<br />
<br />
<br />
<br />
中国近代的革命，就孙中山他们来说，就是用一个概念来套中国，最后当然是不成功，中华民国也成了北洋政府的民国。共产党一开始也是用马克思和苏俄的东西来套中国，当然也是失败，还多亏中国出了一个毛泽东，毛运用山沟里的马列主义修正了那个来自俄国的概念，用孙子兵法水浒传和三国演义搞中国的革命，用土地革命来整合农民和组织农民，这样共产党才获得了成功，也就是说，毛是用经验论和实用主义的办法改变了法国大革命和苏俄的做法，共产党才得以成功。但毛在建国后，又开始按一些的理想性的概念来试验和来改变中国，试图回到法国人的唯理论思维中去，当然最后是失败了。而邓恢复的就是经验论和实用主义，这也是毛一贯的思维方式，只不过毛自己后来背离了自己的正确的思维而已。<br />
<br />
<br />
<br />
<br />
现在中国搞民主搞自由人权，也不能靠法国的唯理论的思维方式，而还是要用中国人自己的经验论和实用主义，可以参考英美经验论和实用主义好的方面，来搞中国自己的改革。也就是，无论搞什么，必须从中国自己的历史文化和现实出发，而不是从一个美好的概念出发</div>

							
							
							
							
