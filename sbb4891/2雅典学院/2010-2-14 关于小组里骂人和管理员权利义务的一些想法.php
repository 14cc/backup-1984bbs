													<h2>关于管理员权利义务</h2>
						
						
																											<div id="postmessage_479790" class="t_msgfont">1. 一方面我同意张书记说的，1984bbs的定位可以是私人客厅；所以管理员有权利较随便的处理一些问题，比如封禁id等等，而不应受到干涉。<br />
需要注意的是，这个定位应该在组员能看到的地方写清楚，因为一般的论坛的定位不是这样。而很多用户注册的时候都是以一般论坛的定位来理解的，就像我在google注册了一个帐号，google有义务在我不做违规的事情时保证我的权利，而且google有义务告诉我哪些是违规的事情。<br />
我现在已经忘了注册小组时看到的term of use，现在大致找了一下也找不到。<br />
可能有人说这是不重要或者不言自明的，但事实上现在的很多纠纷都来源于这一点，所以有必要让组员清楚。这样小组就给了组员了解足够信息用脚投票的自由，而不会出现很多人理解错了定位，进来了投入了却感觉被骗。<br />
2. 另一方面，如果我是组长的话，也不愿意把这个定位写得清楚，甚至不会选择这个定位。<br />
因为小组里还是有很多有理想有知识的人才的，以后可能在社会里起到重要的作用，尽可能的把他们聚集起来，通过讨论和思考，对于组员和社会都是有益的。而“私人客厅”这个定位会影响到小组尽可能的发挥自己的（可能重大的人文社会）作用。<br />
<br />
我是从自己的认识出发，试图讨论什么是“好”的制度，但这可能极大增加组长和管理员的工作量和成本，甚至违背他们的初衷，这些都是我无法得知的信息，所以不好评判。<br />
不过觉得这是一个在小范围尝试民主制度的机会，也是让1984bbs发挥更大作用的机会，要是错过了还是很可惜的。</div>

							
							
							
							
