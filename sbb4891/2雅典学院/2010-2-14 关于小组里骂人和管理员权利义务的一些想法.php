													<h2>关于小组里骂人和管理员权利义务的一些想法</h2>
						
						
																											<div id="postmessage_479775" class="t_msgfont">我是学经济学的，主要学习和关注一些政治经济学，社会学<br />
只在这里从功利主义的角度说一说关于小组里骂人和管理员权利义务的一些想法<br />
<br />
关于骂人：<br />
- 骂人是经济学里最明显的有“负外部性”并可能得到改进的东西。<br />
你骂我，通过给我带来痛苦自己得到一点点快乐；而相应的我骂你也是用你的痛苦换来我的一点快乐，到最后的结果是双方都不开心，不理性，降低了讨论的质量。就像企业污染环境一样，如果通过管制可能使得大家都得到改进。<br />
- 社会对这类问题的解决办法：通过习俗或者声誉的方式，给予骂人的人以额外的代价以抵消掉他所获得的一点快感，于是骂人变成了用自己的痛苦换来别人的痛苦的事情，大家都不会做了。我个人的感觉，刚来小组时，看到随便骂人的人会觉得他是自掉身份，是不理性的人，对他以后的发言也不在意；而绝大多数人意识到这一点，就不太会主动骂人。<br />
- 而声誉机制在人多之后变得薄弱，这样就需要强制的规定（不许骂人）来实现帕累托改进了。社会契约论者可能会说，社会成员舍弃掉自然自由，让渡给集体，以换取契约自由。也可以说将损失骂人这个“积极自由”LIBERTY TO…，得到不被随便骂的这个“消极自由”LIBERTY FROM…<br />
- 当然有的人也会觉得骂人的“积极自由”更加有价值。但是根据绝大多数人对人性的理解和社会的观察，几乎所有人都选取加入保证不被随便骂的“消极自由”的社会。这样能够带来帕累托改进：大家心情更好，讨论更加理性，最重要的是学会compromise，学会尊重他人的意见和自由，而不是把自己的观念强加给他人（很多时候的骂人就来自于强加观念的欲望不能得到满足），而这些都是对民主社会有益的素质。我们为什么不从现在开始锻炼呢？<br />
<br />
所以，如果我是书记，从对组员的利益进行帕累托改进的可能性出发，从提高小组讨论水平的角度出发，都应该对骂人采取一些管理。<br />
甚至有意思的是，可以在这个方面尝试搞一些民主尝试（可能会失败，但是试试无妨，可能会通过参与提高大家在细节上的民主素养）<br />
- 组员可以针对一些纠纷进行appeal<br />
- 每隔一个月由大家投票选出一个5-10人的group，处理这些appeal（他们可以投票，或者为了程序上可行甚至可以一票否决-封禁）<br />
- 如果某人确实处理appeal确实一贯偏颇，组员可以开启投票进行弹劾，如果有100人以上投票且绝对多数同意弹劾的，此人下台<br />
- 如果弹劾不成功，仍然有机会在下一次选举时把ta选下来<br />
小组里很多人都对民主有思考和理论认识，但是做做时间，体验一下“民主的细节”，也是有所收获的。</div>

							
							
							
							
