													<h2>韩寒“没文化”</h2>
						
						
																											<div id="postmessage_757203" class="t_msgfont">先请韩寒的粉丝们注意，我这标题是加了引号的，并非我所说，是转述台湾那个陈文茜所言。冤有头，债有主，要骂人请找准苦主。<br />
&nbsp; &nbsp; 入题之前，我还要作一点申明。首先，我并非想借韩寒上位出名，因为借人之力者自己终究也要有点基础，在出名这一条路上，我基本上一穷二白，所以也不奢望。譬如我在新浪开了个博客，五年来点击量不足五千（一半还是自己点的）。其次，我也没有一点嫉妒韩寒的意思，因为差距实在太大，而嫉妒也须在一定的距离内，例如酸葡萄心理，也是因为那葡萄看起来似乎够得着。<br />
&nbsp; &nbsp; 出于谨慎的态度，我想还应当先说说韩寒的长处。在我看来主要有这样几点：第一、长得帅。最近有记者采访韩寒，问他自己觉得与郭敬明比谁更帅，他以一种很轻松的玩笑口吻说：“我帅。”说实话我也这样认为。第二、很机智。他有一种经常以出人意料的方式运用文字的本领，可以把一些原很平淡的事情说得跳宕幽默，很吸引人。第三、很有个性。在中国这样一个社会里，这一点往往表现为一种与主流格格不入的态度，我想也是在这一点上他获得了很多所谓精英的青睐。<br />
&nbsp; &nbsp; 入题前扯了这么多，足见我对韩寒的影响力是十分敬畏的，批判孔老二我都没有这样谨慎过。下面开始本文主题。我想还是依照上述的那三点来讲讲我的意思，因为换一个角度，长处经常反成为短处。<br />
&nbsp; &nbsp; 第一、在我看来，长得帅对于那些喜作青春偶像的人来说或是一个天生的资本，但对韩寒却未必是好事，因为结果可能反而喧宾夺主。我这样说决不是毫无根据，譬如我随手打开韩寒最新的一篇博文《亚细亚的孤儿》，引几句最靠前的留言：“我终于靠前了！”“竟然第二个。”“韩哥，我的第一次，哈哈。”“激动中，继续占位。”“第一次那么近。”……洋溢着一片激动之情，但是把韩寒当青春偶像来崇拜的人是不会关心他的文章的。对韩寒来说，幸还是不幸？<br />
&nbsp; &nbsp; 第二、机智与真正的智慧是有区别的。机智是一时轻松的莲花妙语，而智慧的背后是严肃的思考和深入的洞见；机智容易流于轻浮，而智慧却要能够耐得住寂寞。譬如韩寒在博文《看孔子》中说：“在中国的古代，产生了一堆的子，虽然他们今天曰的和昨天曰的甚至还会自相矛盾，他们的意义不在于他们说的足够好，而在于他们说的足够多，各个时空的政客们各取所需，或推崇或批判。”这段话就说得很机智，但显然经不住严肃的思考，而且我由此估计韩寒对那“一堆的子”其实缺乏真正的理解。说实话，我对韩寒在赛车之余还能有多少时间和精力去进行文化上的积累是有些怀疑的。当然，这无关他所选择的那一种生活方式，但对于他所承担的某种社会角色我以为是有妨害的。<br />
&nbsp; &nbsp; 第三、韩寒的个性。在儒家文化的氛围里，韩寒的个性确实具有某种程度的批判意义。但是，对此我想说两点：第一、当年他退学著书，一举成名，这其中有多少是因为他书写得好，有多少是因为他退学著书这一行为对当前僵化的教育体制所具有的批判价值呢？换句话说，韩寒在文化批判上的价值到底是来自于他特立独行、充满个性的生活方式还是他作为文化人所提供的思考本身？而如果是前者，我们就不能高估他的批判价值，因为他的生活（包括他的成名）都有太多戏剧性因素，别人摹仿不来。第二、保持住个性在中国是一件很不简单的事情，所以在这一点上我还是很佩服韩寒的。但我也有点担心，因为在传承数千年、至今还保持强大惯性的儒家文化氛围下，保持自己的个性是要有深厚文化支撑的。尤其是年岁增长，名气越大的时候，就越容易变得圆滑世故。事实上，我发现韩寒也正在收缩他的棱角。我姑妄言之，你姑妄看之。<br />
&nbsp; &nbsp; 所以，我其实对陈文茜那句评价还是有些认同的（当然不包括她说“放屁”的那句）：“作为一名意见领袖，应该好好珍惜自己的话语权，充实自己的知识储备。”</div>

							
							
							
							
