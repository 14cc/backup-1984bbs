<br />
<font color="red">日本有权统一中国</font><br />
<br />
<br />
对于日本人，我们从小所受的教育留下的印象就是一个野蛮残忍，丑陋肮脏的形象，直到现在，大家在国内外的各中文网站上经常都可以看到许许多多的中国人包括愤青爱国贼甚至很多右派民主人士，对日本的种种恶毒的漫骂，和带有极端种族歧视的言论，以及战争叫嚣，但是随着年龄的长大，接触到的真相越多，我越来越觉得，日本人民才是真正善良智慧的人民，日本才是一个真正伟大，正义的国家。 <br />
<br />
成则为王，败则为寇。日本正是在这样一种悲剧性的历史命运下，被妖魔化成了一种邪恶的形象，一个历史的替罪羊。关于日本的种种所谓“罪行”，其实都只不过是在被战胜者垄断了话语权的弱势地位下被强加的罪名，完全违背历史事实的蓄意歪曲。 <br />
<br />
例如，被中国媒体反复炒作的所谓“南京大屠杀的罪行”，其实真正深入研究过那一段历史的人都知道，其中包含了很多虚假的信息，比如说，被中国媒体反复刊登的许多用来证明“大屠杀”的所谓照片，很多都是伪造的假照片，比如说被中国官方斩首的马贼照片，被用来作为是日军屠杀平民的证据；国民党守城军队临阵脱逃时不顾老百姓死活封锁出城道路造成大量平民拥挤践踏而死的照片，也被用来作为证明是日军集体屠杀的证据；更恶劣的是，中国媒体竟然无耻到把通州事件（南京事变之前中国军队在通州屠杀日本侨民妇女儿童的事件）的照片也用来作为日军屠杀的照片，这就好比强盗偷抢了别人的钱财反而倒打一钉耙说自己被受害人抢劫了。还有那个臭名昭著的张纯如，在她那本胡编乱造的书中也蓄意地假造，引用了大量的伪证据（真是死有余辜）。 <br />
<br />
其实还在我很多年前第一次看到“南京大屠杀”的所谓屠杀平民的照片时，我就很有疑惑，因为照片上很多被杀的所谓“平民”都留着短短的平头，而当时是冬天12月份，南京的气温很低，南京的老百姓作为首都居民也不可能都留着平头，很显然，这些被杀掉的留着平头的人都是混入平民中的军人，在这种鱼龙混杂的混乱局面下，为了防止这些混入平民中的中国军人搞破坏，日军杀掉一些这样的人也是无可厚非的，当然这个过程中也难免误伤一些平民。 <br />
<br />
既然是战争，无一例外地都不可避免有平民的死亡，就连号称是最人道的仁义之师的美军，在攻打伊拉克的一场小小的局部战争中，尽管采用了种种尽可能减少平民伤亡的战术和高科技技术，短短的几个月中，也还是造成了数以万计的平民伤亡。更何况是六十年前，在当时技术手段落后的情况下，在长达十几年的全面战争中，在面对腐败残暴的国民党军和阴险狡诈的共产党军队的种种蓄意破坏的情况下，战争中误伤了几百万平民，并不是什么很过分很严重的行为，更何况这其中有很大一部分都是属于被中国军队蓄意破坏造成的平民伤亡，例如国民党军掘开黄河淹死的上百万平民，拉壮丁充军造成的劳累而死的大量无辜平民，和共产党军队在日军管理的和平区域搞破坏，埋设地雷炸弹，造成的大量无辜平民的伤亡，凭什么要把这些伤亡也算在日军的头上？ <br />
<br />
至于中国官方一再夸大，不断翻番的中日战争中的数千万“死亡人数”，我是从来都不相信的，不仅因为中国官方一贯撒谎成性，而且更是因为中国官方根本拿不出任何可靠的证据，连个身份统计都没有，凭空就从地底下冒出了几千万子虚乌有的“死亡军民”，而且一天一变样，而人家日本在二战中战死的军人都是一个一个地在靖国神社的牌位中有姓名身份登记在册的。 <br />
<br />
事实上，倒是有大量证据（包括照片和回忆资料）证明在华日军中绝大多数人都是纪律严明，爱民仁义的，跟驻扎当地老百姓的关系也是很融洽的，因为日军的纪律比起腐败的国民党军来说是好得太多了。很多地区，比如说河南，河北，东北的很多乡下老百姓也对当年日本驻军怀有很好的印象，甚至还主动帮助日军缴中国军队的械，帮助日军抓捕搞破坏的游击队，只不过这些都被禁止公开发表。经过几十年的洗脑，现在的中国人都以为日军真的都是凶狠残暴的刽子手。 <br />
<br />
日本可以说是历史上所有与中国交过战的国家中对中国最为宽仁的一个，如果不是美国多管闲事，横插一杠打败日本，破坏了日本统一东亚的王道大业，使得我们中国错误地沦为苏联输出的共产主义势力的范围，那么我们中国在后来的几十年会避免很多的苦难和挫折，现在也不会堕落到这种绝望的腐败境地。 <br />
<br />
我对日本人民，从一开始的误解而产生的极端憎恨到后来了解了真相逐渐觉醒，从而怀有深厚的感情。我相信很多中国人，特别是东北人民，通过对比东北在日本妥善管理下的迅速发展，和后来沦为共产党势力下的迅速衰败，都应该深有同感。 <br />
<br />
日本人民的宽仁，温和，善良，守礼，守时，遵纪，守法，勤奋，严谨，诚实，都是我所敬仰的，日本人民的优点数不胜数。 <br />
<br />
无论是从人种上还是从文化上，都可以说，日本才是古代中国文明的真正继承者。种族上，日本属蒙古利亚通古斯人种，跟古代中国的初始文明商王朝的商民族是同一民族，因为商来自于东夷，而东夷就是通古斯人种，日本朝鲜韩国，都是东夷的另一个分支；从文化上，日本继承的是原汁原味的汉唐文化，而现代中国继承的是满蒙游牧文化和南方百越南蛮的混合杂交文化，日本文化才是真正意义上的古代中国文化。 <br />
<br />
所以日本统一中国的战争本质上是与几千年来北方通古斯黄河文明统一南方百越南蛮的战争一脉相承的，日本抵御苏联共产主义势力的扩张在本质上也是与几千年来黄河文明抵抗北方游牧民族侵略的战争一脉相承的，是正义的，是具有历史进步作用的，是先进文化战胜落后文化的正义战争。 <br />
<br />
