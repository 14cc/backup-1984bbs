另外寂寞小手网友虽然提到她的外公被日本人杀掉了，但是却没有告诉我们她的外公到底是因为什么原因被杀掉的。因为被杀也有很多种情况和原因，有在战场上战死的，有被流弹误伤的，有参加游击队搞破坏被击毙的，如果是后一种情况，那我就很难同情她了。难道伊拉克恐怖分子搞破坏被美伊联军击毙了我们也要去同情他们吗？ <br />
<br />
再说了，中国网友中，外公被中国人自己杀掉的那就更多了，难道我们也要为此去仇恨中国人自己吗？所以说这没有道理。 <br />
<br />
在这里，我也顺便提一下，我的外公没有被日本人杀掉，但是，我的爷爷，很不幸，他却是在文革中被自己的“中国同胞”害死的，按理说我应该去仇恨中国人，但是我没有，所以我也实在是想不出我有什么理由要去仇恨日本人，难道因为寂寞小手网友你的外公被日本人杀掉了，所以我就应该跟着你一起去仇恨日本人吗？你又没有给过我什么好处，你的外公被日本人杀了关我们什么事呢？我为什么要跟你一起去仇恨日本人？中国人害死了我的爷爷，我都没有仇恨中国人，我为什么要去仇恨跟我无怨无仇的日本人？这不是很荒谬吗？所以说这没有道理。 <br />
<br />
比起爱国贼们来说，我们虽然不准备去参与仇恨攻击日本人的流氓活动，但是对在日本统一中国战争中被误伤的人还是给予同情的。但是爱国贼们往往就没有这么高尚的道德素质，对于在肃反，土改，反右，大饥荒，文革，六四中被中国人自己杀掉的中国人，他们往往不仅不同情，而且还幸灾乐祸。从这个角度来看，从站在两种不同立场的人的道德水平高低就可以看出这两种不同立场的对错是非。 <br />
<br />
还有某些不知名的网友，阴阳怪气地说什么“日本要统一中国那就全面开放海关关口，解除移民，就业等限制”，我看这些人是故意混淆是非，而且也是对历史的无知。在日本统一中国的战争进行之前，日本就是对中国人全面开放的，当时的中国人不需要办任何手续，只需要买一张船票就可以自由进出日本，很多在中国找不到工作的人，被政府迫害的人，想要学习西方科学文化的人，都纷纷大量涌入日本，而日本政府也尽力地给这些人提供就业机会，政治庇护，学习补助（如我们都熟悉的鲁迅，孙中山，周恩来，郭沫若等人）等帮助，可以说当时日本人民对中国人是非常友好的，做到仁至义尽。 <br />
<br />
实际上，中国在近现代的所有进步和现代化的进程，可以说大部分都得益于日本的影响和帮助，辛亥革命是在日本的影响下产生和日本政府的资助下得以成功的，孙中山，黄兴等革命党人都是依靠日本政府的政治庇护和资助来开展革命工作的；鲁迅，周恩来，郭沫若等人都在日本留学，依赖日本人民的补助学有所成的，不仅他们，而且影响中国历史和文化发展的大批文人，学者都是在日本留学，受到日本文化和思想的熏陶，影响；现代汉语中的大部分词汇都来源于日本，中国赖以实现现代化的很多西方思想，体制也是通过日本作为媒介传播到中国来的，可以说，如果没有日本人民的多方帮助，就绝不会有现代意义上的中国。 <br />
<br />
还有日本对中国的所谓“战争赔款”的问题，尽管中国官方出于统战需要，自己主动放弃了赔款，虽然日本当时也处在非常困难的境地，但是当时日本却主动地一再提出要给予中国补偿，而且在中国官方假惺惺地放弃了赔款之后，日本人民仍然尽可能地以多种途径给予中国人民帮助，如大量的低息贷款，这大量的低息贷款在中国的改革事业中所起到的决定性的作用实际上早就已经远远超过了国际法庭强加给日本的所谓应偿付的“战争赔款”，但是中国官方对此却隐而不提，从没表示过半点谢意。 <br />
<br />
如果不是日本人民对中国的多方帮助，我们中国人现在恐怕都还在啃泥巴呢。 <br />
<br />
日本对中国只有数不尽的恩情，没有欠债！ <br />
<br />
某些中国人喜欢用所谓的“国际法庭”的“宣判”来证明日本在统一中国的战争期间犯下了所谓的“罪行”，我认为那是非常荒谬的。这个所谓的“国际法庭”的所谓“宣判”，完全只是战胜者对战败者的单方面强加的罪名！根本没有公平性和客观性可言。 <br />
<br />
稍有法律常识的人都知道，在法律审判中，原告和被告都应该处于一个平等的地位上，判决只能由中立的第三方来进行，如此才能保证判决的公平性和客观性。但是这个所谓的“国际法庭”对日本的所谓“审判”，战胜者一方即充当原告，又充当判决者，这是完全违背法律常理的。其所宣布的所谓“罪行”完全只是战胜者对战败者的单方面强加的罪名！根本没有公平性和客观性可言。 <br />
<br />
其实就连这个非法的所谓“国际法庭”也对二战后对日本的“审判”过程中，中国官方胡编乱造种种罪证诬陷日本的行为感到不齿，故此强行把中国官方提供的所谓30万“南京大屠杀死难者”数目强制压缩到了20万，这个数字的误差率竟然达到了百分之三十！但是出于中国蒋介石反动政权是战胜者的成员的压力下，还是违心地接受了20万的数字，从此这个罪名就被强行加在了日本人民头上。 <br />
<br />
如果战胜的一方是日本，毫无疑问地，站在被告席上的就将是蒋介石国民党反动政权。而我们今天要接受的就将是蒋介石国民党反动政权对抗统一，分裂中国的历史罪名。 <br />
<br />
在这一历史过程中，可以说，美国扮演了一个错误的角色。这是我一直以来都很痛心的一件事情。 <br />
<br />
其实美国和日本都是近代以来对中国最为友好，帮助最大的两个国家，美国和日本可以说是中国的两个大哥哥，对于中国这个不争气的小弟弟，这两国一直都试图用自己的方式来帮助中国发展进步，但是两国在具体的方式上却有所分歧，最后导致两国反目成仇，同室操戈，并最终给了苏俄共产主义势力可乘之机，使得我们中国错误地沦为苏俄输出的共产主义势力的范围，使得我们中国在后来的几十年中经历了很多的苦难和挫折，致使现在堕落到这种绝望的腐败境地。这是我一直以来都很痛心的一件事情。 <br />
<br />
好在后来美国和日本终于又重新和好，携手共同抵御了苏俄共产主义势力的扩张，并最终打垮了这头恶魔。我们希望，在将来，美日两国能够再接再厉，继续努力打垮中国共产党这个最后的邪恶势力，希望美日联军能够早日解放中国，拯救中国老百姓于水深火热之中。 <br />
