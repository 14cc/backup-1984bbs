<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; 江湖雀语：今日新闻说捧杀<br />
<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; ・美国畅销《当中国统治世界》，根据数据要中国负更多的经济责任，企图捧杀中国。<br />
&nbsp; &nbsp;&nbsp; &nbsp; ・中国人均收入3,600万，排在世界百名之后，不到富国10%。尚有贫困人口1.5亿。<br />
&nbsp; &nbsp;&nbsp; &nbsp; ・达沃斯会议上，有外国经济学家认为：中国应该培养自己的消费者，做到自给自足。<br />
&nbsp; &nbsp;&nbsp; &nbsp; ・海南三亚的酒店17,480元一夜，照样不能阻挡炒房客，春节爆满。<br />
&nbsp; &nbsp;&nbsp; &nbsp; ・深圳大冲村拆迁，造就千万富翁400，亿万富翁10余。<br />
&nbsp; &nbsp;&nbsp; &nbsp; ・《房屋征收与补偿条例》征求意见稿公示。最大进步为：改“拆迁”为“搬迁”。<br />
&nbsp; &nbsp;&nbsp; &nbsp; ・最高检规范检察人员工作用语。“驻京办”不到数千。“世博”上海禁止室内吸烟。<br />
&nbsp; &nbsp;&nbsp; &nbsp; ・四川巴中规定领导每月必须回乡住宿两天以上，联系基层，开展群众工作。<br />
&nbsp; &nbsp;&nbsp; &nbsp; ・全国两会讨论“低碳”问题，发电子书，提倡离开宾馆关好水、电，勿用电脑娱乐。<br />
&nbsp; &nbsp;&nbsp; &nbsp; ・农村新貌：家有分装垃圾桶。彭山推出刘备墓，称：旅游就是小题大做，无中生有。<br />
&nbsp; &nbsp;&nbsp; &nbsp; ・铁道部称：春运要让旅客走得掉，走得好……如电话订不上票，请勿到车站排队……<br />
&nbsp; &nbsp;&nbsp; &nbsp; ・许多学校的班主任写学生评语，用电脑软件，一分多钟可完成两百余字。<br />
&nbsp; &nbsp;&nbsp; &nbsp; ・全世界都有“蜗居”者：日本有“胶囊旅馆”；美国有“帐篷露营”……<br />
&nbsp; &nbsp;&nbsp; &nbsp; ・我国人均石油不到世界平均的六分之一，以进口为主。煤炭也依靠进口。<br />
&nbsp; &nbsp;&nbsp; &nbsp; ・我国成立“能源委员会”是权力从集中到分散，又从分散到集中的，多次循环往复。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 对自己的成就“无比自豪，无比骄傲”……以及“支援亚非拉革命，尽国际主义义务”，从来都是我们中国的特色，不意这次在达沃斯，面对人家的夸奖我们却像挨了骂一样。为什么？央视的专家说：我们又不是欧美……中华民族从来没有管别人家事务的传统，我们还是管好自己的事情……<br />
&nbsp; &nbsp;&nbsp; &nbsp; 哈哈，原来我们公布的数据太牛，引得人家来要求我们尽责任，担义务，于是，我们只好哭穷，不管他人瓦上霜了。<br />
&nbsp; &nbsp;&nbsp; &nbsp; 试想，如果隔壁有这么一位，大家会对其作何感想？难道会加以热情的捧杀吗？<br />
&nbsp; &nbsp;&nbsp; &nbsp; 我们借外国专家的嘴说：我们只能自给自足。这可是延安的口号，而延安没有人住得起一万七千多块一夜的酒店，更没有什么人因为拆迁，变成千万、亿万富翁。看来是我们的牛气数据没有反映出贫富不均的事实来，所以，让外国佬和我们中国老百姓全都误会了。<br />
&nbsp; &nbsp;&nbsp; &nbsp; 这些年来，与其说我们中国有多大的建设成就，还不如说有多大的盘剥老百姓的成就，或者说有多大的吹牛皮的成就。比如众所周知的财政收入靠拆迁，拆迁靠置民于死地。现在群情汹汹，压力下只好改变，但最大的改变也不过是换一个词：拆迁改“搬迁”。看看“条例”中对公共利益的界定，几乎包括了现行拆迁的一切情况，还要留下“其他”的后路以备不时之需。我们的政府真正在“进步”的，也就只有规范工作用语、把不利于己的数据缩小、禁止室内吸烟之类……当然，连室内禁烟都是看在外国佬的份上。不然，顶多也就是像巴中，让领导每个月回家多休几天带薪假，玩点假公济私的把戏，哄哄老百姓了事。<br />
&nbsp; &nbsp;&nbsp; &nbsp; 这种状态的形成，无疑是因为社会的运行规则跟不上时代文明，也正因此，我们的精英级代表们，可悲到连离开宾馆前关好水、电都要讨论，倒是公款发电子书无需讨论，照单全收。所以，在他们的带领下，没有分类垃圾处理厂的农村，偏偏有分类垃圾桶；旅游成了骗人上当；“春运”在买不到票的情况下，被叫做“走得掉，走得好”；学生的评语不再用班主任费心；连买不起房子也成了国际时尚……以至能源的困境，也就成为理所当然了。<br />
&nbsp; &nbsp;&nbsp; &nbsp; 反正，治国无非是权力的分散与集中：权在谁手，利在谁口；权利结合，民生乌有。<br />
&nbsp; &nbsp;&nbsp; &nbsp; 最有趣的是，世情已然如此，我们中国的老百姓还是被称为：国家的主人翁。<br />
&nbsp; &nbsp;&nbsp; &nbsp; 唉，这才是货真价实的“捧杀”啊！<br />
