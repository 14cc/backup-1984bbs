													<h2>炮轰右粪的五大荒谬观点</h2>
						
						
																											<div id="postmessage_471169" class="t_msgfont">现在空闲了一会，说说右粪经常发布的令人发指的几大观点！就因为这几个不切实际的说法，我坚决不和他们站到一起，因为那样的话，我认为是我的耻辱<br />
　　<br />
　　 我们在评论左粪以及右粪的时候，一定记住，这些家伙的观点中有部分正确的东西，但是里面夹带了太多的私货，这些私货如果不好好判断，以为和好东西在一起就是铁饼一块，那么很多人就会被它左右，丧失了原则和立场。<br />
<br />
　　 当然原则和立场会随着时间以及阅历的变化而相应变化，这是可以允许的。<br />
<br />
　　 右粪的第一大观点――全面否定邓毛：<br />
　 我给老毛的定性和老邓的观点一致，认为是三七开，有些时候看了张志新以及林昭等人的事迹，我甚至想五五开，但是综合看来，这个农民还是很厉害，有一套水平的，所以暂且同意老邓的划分。<br />
　 至于老邓，我给的分非常高，我是这个小个子四川人的坚定支持者<br />
　　 人无完人，金无足赤，不可能没有犯过错误的人，很多人抓住一个小错误说一辈子，全面否定这个人做出的所有成绩，我对于这样的观点坚决不认可。<br />
　<br />
　　 克林顿作风不好，但是不影响他是美国的好总统，成龙作风也不好，但是我一样喜欢他的作品。这些人出来的时候谁也没在脸上贴个标签，说自己的道德楷模，模范先锋，是我们这些好事的，自己给人家册封的，最后自己又给人家剥夺的――你说到底谁可<br />
老毛作风不好，这个世人皆知，我认为只是一个小瑕疵，这是人的本性，以后大家最好把这个作风和人的能力功绩区分开。<br />
　　 老毛的主要成绩我看在于把全国该统一的地方统一了，消除了地方割据（台湾除外），灭除了军人参加政治的影响，而且打出了中国人的威风，让我们在野蛮战争方面头一会站在了世界前列（以前的什么秦皇汉武不做数，成吉思汗还有争议）。<br />
　　<br />
　 右粪的第二大观点――全面否定当前取得的成绩：<br />
　　<br />
　 这些右粪认为目前国家在经济上取得的成绩微不足道，还是有大批的下岗职工，还是有大量的贫困地区，而这一切的根源在于中国的制度不行，中国的政*府无能――左派认为如果还是毛时代，这个现象可能小时，右粪则完全认为是意识形态惹的祸。<br />
　　 我不这样看，我不是什么经济学家，我也不是什么权威学者，但是我明白，国家和国家之间，同商家与商家之间的竞争是一样的，很多人做买卖赔钱，选项目赔钱，国家也如此，不是每个国家做买卖都能挣到钱的，右粪一整就说中国是依靠出卖廉价劳动力发家的，是世界名牌的后院加工厂，认为这是一个耻辱。我不这么认为，有一天，没有人使用我们的廉价劳动力，世界也不在中国加工产品，你们认为我们没有了右粪所说的耻辱之后，我们的生活会比蜜甜吗？<br />
<br />
　　 我不认为！<br />
<br />
　　 每个国家情况都不一样，世界上有廉价劳动力的国家有的是，非洲每年饿死那么多劳动力，我看也没有哪个500强企业去大量利用。<br />
　　 中国13亿人，假设我们国家现在只有5亿人，假设还是这个GDP，你们还会认为我们国家老百姓象现在一样穷吗？<br />
　　 说良心话，我认为会进步一些。<br />
　　 目前国家取得的成绩，我是有感受的，我绝对不回到文革那时候去和左粪有难同当，我也绝对不向往右粪描绘的西方天堂。<br />
<br />
　　 右粪的第三大观点――为国民党翻案<br />
　 不少右粪在文章中提到以前的抗战，以及内战，都是使用国军，共军等词眼，然后说出解放前共党如何搞破坏，例如大量印制假钞，是导致蒋统区通货膨胀的罪魁祸首。<br />
我要肯定的说，国民党中有很多精华，当然其中也象gcd那样有不少糟粕。国民党在抗战中确实浴血奋战过，而且我认为作用要远远大于八路军，但是历史就是如此弄人，最后你失败了，而且失败的非常彻底，几乎没有什么翻身的机会，那么我们可以正视你的功绩（不再丑化你，污蔑你），但是绝对不代表你就全是好样的。<br />
　<br />
　　 共党有缺陷，我这都承认，而且很多手段不能见光，例如那个半夜鸡叫，还有刘文财的收租院确实臆造的比较无耻，让我小时侯始终认为资本家和地主都是黑社会，后来一长大，发现其实资本家和地主一样是人，欺负人的毕竟是少数。<br />
<br />
　　 蒋介石自己都承认自己的官员腐败到家，你们还翻什么啊？历史上有成绩，就能证明全正确吗？<br />
　 国民党即使在台湾也是小蒋之后才获得长足的发展，老蒋在的时候，国家经济也一般，擦亮眼，别胡说八道，好象国民党要是不败退，你们现在的生活条件就会和台湾一个样似的。<br />
<br />
　　 右粪的第四大观点――不切实际，盲目追求西化<br />
<br />
　　 我承认美国等西方国家的制度比我们完善，比我们有优越性，是我们学习的好榜样，而且也希望明天早上起床就直接可以去国会嫖女秘书，但是同志们，不行啊，咱们国家现在根本没有这种精英阶层，就目前这些在各大论坛混迹的所谓开天眼明断是非，上下预测500年的左右精英们，还始终互相内讧干仗呢，要是在国会里见面，不说骂的多难听，匕首铁锹都准备好了，不见血不带松手的。<br />
　　<br />
你见过议员带铁锹去开会的吗？<br />
　　<br />
　　 我们的儒家思想本身就不是一个法制的思想体系，他提倡的什么三从四德，光宗耀祖，忠君爱国的逻辑本身就是扯淡，但是我们国家现在主旋律不是还一直推崇吗？那么多人近世，就你明目，你得有多少耐心去对牛弹琴啊。<br />
这种自私自利的作风应该是全国从上到下蔓延的，在这种土壤上建立精英阶层，我认为至少还需要50年，等国家开始不重点使用那些只会埋头吃苦肯干，唯领导马首是瞻不用脑子思考的干部的时候，我认为希望可能才会有。　 西方的议会制度，选举制度也有很多不是很成功的地方，例如说印度，墨西哥，以及中美洲，非洲包括亚洲的一些资本主义国家，他们的成败各有特色，有的是也解决不了贫富差距，有的是没有集权，国家内讧搞割据打群架，另外就是打着民主自由的旗号不断搞政变，，每次都信誓旦旦发家致富，可到最后还是腐败透顶。<br />
　 我们注意到成功的资本主义和失败的资本主义的一个比较典型的差距――那就是国民素质。<br />
<br />
　　 我们国家的国民素质我不认为比印度或者非洲那些灾民强到哪里去――随地吐痰，留须拍马屁，走后门，自私自利。<br />
　　 昨天看到一个美国电影，《黑皮书》，描写二战时期荷兰一个犹太女孩在1944年侥幸没有进入集中营，去找一个荷兰律师去取父亲之前委托托管的财物，人家律师很负责任，将财产――壹大包钻石给了落难女孩，我就想这是1944年的荷兰，如果在当代中国，我不认为我周围这些同行里能有几个手下留情不留下点买路钱的。<br />
<br />
　　 进化吧，中国人进化到嘴开光的时候，民主自然就来了。<br />
<br />
　　 右粪的第五大观点――无政*府主义，意识形态大跃进 <br />
<br />
　　 我写了几个文章，不少人反驳，其中用马克思的观点谈到国家，政*府等等，认为国家就是统治阶级的暴力工具，最后进入某个莫名其妙的阶段（共产主义），国家就失去作用，消失了，所以我们根本不需要爱国，国家这个概念本身就是虚无的。 这些理论我中学的时候学习过，那个时候不太明白，只是为了应付考试，但是觉得说的很有道理，认为这是真理，但是后来我就不这样悲观的看待了。<br />
　　<br />
　　 国家如果是统治阶级的暴力工具，那么家庭是不是男人占有女人的暴力工具？<br />
&nbsp;&nbsp;很早以前，有个搞笑解释，问什么叫和平，那个解释是这样说的――和平就是两段战争的空隙时间。这个解释很黑色，可以媲美老马的理论，大家能不能悟出里面的猫腻？对了，这两个幽默都残忍的承认了人性本恶这个前提。<br />
　 既然人性本恶，那国家到底是干什么的呢？还能消失吗？<br />
<br />
　　 国家是为人民服务的，世界不会大同，永远不会，只要人性还是恶的，那么谁先大同，恶人就会成为那汤锅里的一泡屎。<br />
　 千万别买弄这点无政*府主义的知识，实在太丢人了。我认为有这个理论的人，是右粪中头脑最短路的，当然也是所谓知识最丰富的，可是严重消化不良。<br />
