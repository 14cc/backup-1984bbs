																											<div id="postmessage_487271" class="t_msgfont">我来告诉你右派对经济危机的原因解释：<br />
<br />
1、哈耶克的看法：我们可以用几句话来概括哈耶克的经济周期理论。首先，他认为，通过信贷市场向经济体系中注入新货币会降低利率水平，从而导致资源的错误时际配置。因为，利率降低后，投资者获得资金的成本下降了，这会人们增加投资。在资本报酬率较高、生产周期较短的投资项目的资金得到满足之后，人们就会把钱投入生产过程较长的新增项目。这些新增项目将对应着新增发的货币，这种信贷资本结构的变化会导致经济活动的净增加，这便构成了一个繁荣的过程。但是随着时间的推移，人们将发现这些还没有变成实际投资的信贷资本结构的变化和实际的资源结构是不相宜的。在进行实际投资的过程中，人们发现某些生产要素会供不应求，这些要素的价格就会上升，成本的上升会进一步增加信贷需求。由于这些成本的增加是投资进行伊始没有考虑到的，这就必然会影响投资的利润的计算，如果此时投资者发现该项投资最终将得不偿失，他们就会撤回投资，这样做的后果是：已经投入的成本很多是不可收回的，这是一种资源的浪费，同时与这些资源相配套的就业人员也将失业，经济将陷入萧条。<br />
<br />
2、真实经济周期理论：真实经济周期理论认为，市场机制本身是完善的，在长期或短期中都可以自发地使经济实现充分就业的均衡。经济周期源于经济体系之外的一些真实因素的冲击，这种冲击称为“外部冲击”。引起这种冲击的是一些实实在在的真实因素，因此这种理论称为真实经济周期理论。市场经济无法预测这些因素的变动与出现，也无法自发地迅速作出反应，故而经济中发生周期性波动。这些冲击经济的因素不产生于经济体系之内，与市场机制无关。所以，真实经济周期理论是典型的外因论。 <br />
<br />
　　真实经济周期理论把引起经济周期的外部冲击分为引起总供给变动的“供给冲击”和引起总需求变动的“需求冲击”。这两种冲击又有引起有利作用、刺激经济繁荣的“正冲击”(或称“有利冲击”)，以及引起不利作用、导致经济衰退的“负冲击”(或称“不利冲击”)。有利的冲击比如技术进步，这种冲击刺激了投资需求；不利的冲击比如上世纪70年代的石油危机，对供给有不利影响，类似“9・11”这样的事件也可以归入不利冲击。国内外发生的各种事件都可以成为对经济大大小小的外部冲击，但其中最重要的是技术进步。在引起经济波动的外部冲击中，技术进步占三分之二以上。值得注意的是，真实经济周期理论把政府宏观经济政策也作为引起经济波动的外部冲击之一。 <br />
<br />
　　外部冲击如何引起经济周期呢？我们以技术进步来说明。假定一个经济处于正常的运行之中，这时出现了重大的技术突破(如网络的出现)。这种技术突破引起对新技术的投资迅速增加，这就带动了整个经济迅速发展，引起经济繁荣，技术是决定经济的重要因素之一，近以，这种繁荣并不是对经济长期趋势的背离，而经济能力本身的提高。但新技术突破不会一个接一个，当这次新技术突破引起的投资过热过去之后，经济又趋于平静。这种平静也不是低于长期趋势，而是一种新的长期趋势。上世纪90年代美国经济繁荣与以后的衰退证明了这种理论。经济中这种大大小小作用不同的外部冲击无时不有，所以，经济的波动也是正常的，并非由市场机制的不完善性所引起。</div>

							
							
							
							
