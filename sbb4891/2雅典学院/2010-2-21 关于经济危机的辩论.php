　　经济危机（Economic Crisis）指的是一个或多个国民经济或整个世界经济在一段比较长的时间内不断收缩（负的经济增长率）。是资本主义经济发展过程中周期爆发的生产相对过剩的危机 。是经济周期中的决定性阶段。自1825年英国第一次爆发普遍的经济危机以来，资本主义经济从未摆脱过经济危机的冲击。 经济危机是资本主义体制的必然结果。由于资本主义的特性，其爆发也是存在一定的规律。<br />
　　经济危机是指经济系统没有产生足够的消费价值。也就是生产能力过剩的危机。有的学者把经济危机分为被动型危机与主动型危机两种类型。<br />
　　所谓被动型经济危机是指该国宏观经济管理当局在没有准备的情况下出现经济的严重衰退或大幅度的货币贬值从而引发金融危机进而演化为经济危机的情况。如果危机的性质属于这种被动型的，很难认为这种货币在危机之后还会回升，危机过程实际上是对该国货币价值重新寻求和确认的过程。<br />
　　主动型危机是指宏观经济管理当局为了达到某种目的采取的政策行为的结果。危机的产生完全在管理当局的预料之中，危机或经济衰退可以视作为改革的机会成本。<br />
