																											<div id="postmessage_486714" class="t_msgfont">左派认为经济危机是自由放任造成的，右派认为经济危机是管制造成的。<br />
不过话说回来了，都管制起来肯定没有经济危机，都放任了...当然右派也可能会说“没有”，毕竟世界上没有“都放任”的先例。但这就涉嫌抬杠了。<br />
所以，此事从右派的角度，应该就事论事，不应该把经济危机的全部原因推给管制，那样在大多数人看来，肯定是不合理的。其实左派也占不到什么便宜，因为他们只要敢往”都管制“方面说话（只要有那个倾向），就是金日成、毛泽东。<br />
<br />
左派认为可以用干预的方式处理经济危机，部分右派则反对，认为不应该干预。<br />
这部分右派的说法是很不明智的，因为世界上根本没有哪个政府敢不干预，拿没有的事说事，和共产主义者有什么两样？当然，干预经济危机的手段往往是很不堪的：被救济的经常不是人民，而是资本家。但这个责任要由那些具体干预者去负。<br />
实际上，就算是左派干预经济，只要他不想乘机发动革命，也要考虑经济本身的运行规律。如果他放开资本家不管，只是救济人民，经济体系就会崩溃，大家都完蛋。所以，左派虽然嚷嚷得好听，仍然不能得罪资本家（那样就没人投资了），让他们去干预，正好让他们自己打自己耳光：省得他们天天瞎嚷嚷，把正常人都说成是资本家的走狗。<br />
<br />
当然，也许有人说了：罗斯福救济人民，不是很成功吗？<br />
其实不然，罗斯福的那个时代有特殊性：人民福利水平很低，而资本全球化程度不高，美国资本逃到世界上的其它地方不容易。看看奥巴马，就知道他也主要得救济资本家了。<br />
而且，罗斯福新政的经济指标也不怎么样，比希特勒差远了。</div>

							
							
							
							
