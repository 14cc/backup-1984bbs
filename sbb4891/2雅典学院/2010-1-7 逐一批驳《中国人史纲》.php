																											<div id="postmessage_425774" class="t_msgfont">　　第十三章，纪元前第一世纪。<br />
　　本章，柏杨歪写了苏武留胡的故事：“莫名其妙的怪事就发生在这位丑恶的副使张胜身上，张胜跟早先投降匈奴汗国的一些汉人，密谋乘且囗侯单于外出打猎时，杀掉匈奴的智囊卫律，然后劫持单于的母亲，逃回中国，这种卑鄙计划，竟出自高级外交官员，使人心惊。事情败露后，经过一场流血杀戮，爱国心切的张胜露出投机的原形，投降匈奴。”<br />
　　这个故事和历史记载很像，但里面却包含了故意的歪曲，这种笔法我们都很熟悉吧？歪曲的地方包括：第一、卫律的身份可不仅仅是智囊，而且是叛徒（写智囊多无辜啊）；第二、张胜也不是主谋，主谋是卫律的一个手下，张胜可能连从犯都算不上，仅仅是知情不举而已。所以匈奴才让张胜投降啊，你想想看，如果真是无故搞谋杀的主犯，谁敢留着？<br />
<br />
　　第十四章，第一世纪。<br />
　　在本章中，柏杨写到：“王莽的社会改革是伟大的措施，但他彻底失败，他的生命和他的王朝也跟着一齐丧失。我们可为他归纳出下列五项使他失败的原因：......”因为有这个“伟大”作为基础，王莽虽被柏杨认定是“儒家巨子”，总算没被恶骂。当然也有看起来不太准确的说法，总体来讲倒还好。不过我以为：王莽和柏杨是同一种类型的“伟大”。<br />
　　上一章张胜这样的倒霉蛋被柏杨指被“卑鄙”“使人心惊”，本章的班超卑鄙得手，柏杨笔下班超的事迹是这么写的：<br />
　　“班超说：‘不然，智慧高的人能在危机未发生时，即观察到危机，何况危机已经发生。我判断一定是匈奴使节到达，鄯善王正在仿惶不定，不知道应该追随中国？或是继续追随匈奴？’于是诈问招待人员：‘匈奴使节来了几天？住在何处？’招待人员吃惊说：‘来了三天，住地距此十五公里。’班超召集他的全体部属――总共只三十六人，研究对策。大家说：‘我们在危急关头，生死都听你安排。’班超说：‘不入虎穴，焉得虎子。现在只有一条路，我们乘夜攻击匈奴使节，把他们全部消灭，使鄯善王国得罪匈奴，必须依靠中国。’于是当晚奇袭匈奴帐幕，匈奴使节团一百三十余人，全都葬身火窟。”<br />
　　班超这种事干了不只一件，大费柏杨的笔墨，但是却无一语恶评，殊为怪事。</div>

							
							
							
							
