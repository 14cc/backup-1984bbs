																											<div id="postmessage_422883" class="t_msgfont">　　第十一章，纪元前第三世纪。<br />
　　秦始皇终于统一中国了。为了以示尊重，柏杨不仅把他称为嬴政大帝，还破天荒地写了“秦始皇”这三个字。而紧跟着的汉高祖，仍然只是刘邦，既非刘邦大帝，也非汉高祖。<br />
　　当然，秦王朝和共和执政一样，也很快就完蛋了。对此，柏杨写道：“秦王朝虽然是一个由悠久历史的王国演变出来的政权，但建立大帝国的时间却很短，刚刚进入危险的瓶颈时代，却不幸的出现了一个花花公子当它的舵手。”给大家讲述了一个秦朝不幸灭亡的故事。<br />
　　最后，柏杨总结道：“秦王朝灭亡的如此迅速的原因，我们归纳为下列三项：<br />
　　一、中国古老的法家学派的法治，是以君权为基础的，跟现代以人权为基础的法治，有很大不同。......<br />
　　二、秦王朝统一的时间太短，前后只有十余年，人民对新政府的效忠心理，还没有养成惯性。被剥夺既得利益的既得利益阶级，诸如六个王国的那些贵族和当权阶级跟他们身上的寄生虫，以及大批被排斥的儒家知识分子，每一国以五万人估计，就有三十万人的反对力量潜伏民间，在那里怨声载道。这是一个庞大的数字，秦政府还没有彻底的消化。他们日夜都在破坏新社会和新秩序，巴不得秦政府早日垮台，以便恢复他们过去的那种好日子。所以任何震撼发生，他们都会抓住机会。像孔丘的后裔孔甲，跟一些故鲁国的儒家学者，一听说陈胜起兵，立刻就迫不及待的抱着他们的儒书，前往投靠。<br />
　　三、最后的原因是帝国的舵手赢胡亥，我们不必多强调他，只要了解，无论大船小船，舵手非常重要。舵手如果决心向礁石上猛撞，谁阻止他谁就成了卖国贼而被杀或被投入监狱，再坚固的巨轮都会沉没。”<br />
　　我来评价一下吧：<br />
　　“一”毫无道理，完全是转移视线用的，谁打算比较秦王朝和现代国家啦？法家也好，儒家也好，与现代理念当然都是不同的，这跟迅速灭亡有什么关系？<br />
　　“二”看似有理，实际上是倒果为因的：因为秦国激进，所以反对者众多。而且首义的陈胜、吴广，入关取天下的刘邦都不是旧贵族或儒生，只有项羽是旧贵族。如果非说儒生“立刻就迫不及待的抱着他们的儒书，前往投靠”陈胜，并对秦朝的灭亡起了很大作用。那只能说朝廷不能容忍能人，活该倒霉。<br />
　　一般反儒分子往往要说儒生百无一用，不依附于权势就不能发挥作用。但是说到秦朝的灭亡，反而要算到儒生上。真是咄咄怪事。<br />
　　“三”不过是“大海航行靠舵手”的逻辑。<br />
　　要让我说秦朝灭亡的原因，其实也简单：<br />
　　儒家法家都极力推崇王权，但儒家的推崇形式重于实质（或者至少是并重），即“礼”；法家则要设计一整套制度，让国王（皇帝）能切实掌握权力，从上到下一而贯之。法家的效率当然高，但其制度有两大漏洞：<br />
　　一是一旦王权被奸臣篡夺，或者国王（皇帝）太不像话，整个体制对掌权者就是一点阻碍也没有。平时办事的高效率，这个时候就变成了作恶的高效率。办事效率低点并不致命，作恶效率高就完蛋了。<br />
　　二是法家办事的高效率，完全建立在对相关人员进行人身威胁的基础上。如果相关人员还能混得下去，自然得战战兢兢地把事办好。但如果混不下去了呢？也就只好起义。陈胜吴广起义的具体形式，在中国历史上并不常见，这完全拜法家所赐。<br />
<br />
　　好吧，打住，再写就太长了。最后说两条吧：<br />
　　一、周、秦、汉三朝都是依靠同一块地盘进占天下的，如果不是纯属偶然的话，那么秦国之统一的主要因素...恐怕是地理因素吧？<br />
　　二、汉高祖只比秦始皇小了不到5岁，所以与其说始皇帝灭了六国，不如说始皇帝灭了七国。这七国都有数百年的历史，不被始皇帝这么一折腾，怎么会这么快就“宗庙为墟”呢？</div>

							
							
							
							
