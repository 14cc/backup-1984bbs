																											<div id="postmessage_421004" class="t_msgfont">　　第七章，纪元前第七世纪。<br />
　　在本章，柏杨发明了一个新词汇“尾巴国”：“本世纪，封国间的战争升高，战败者只有两条路可走：一是向战胜国乞和，当战胜国的尾巴国，即所谓接受‘城下之盟’；一是想当尾巴国而不可得，被战胜国并吞。”<br />
　　效仿一下柏杨尖酸的思路，我来估计一下“尾巴国”这个奇怪词汇的来历吧：<br />
　　本来有现成的词“卫星国”或“仆从国”，但是这类描述东欧国家的词出现在这儿，难免要提示人们俄国人的跋扈。作为俄国人的尾巴人的柏杨是断断不能用这类词的，只好发明个新的。<br />
<br />
　　第八章，纪元前第六世纪。<br />
　　在本章，柏杨稍微编造了一点历史：“阳虎在稍后出任鲁国的宰相，负责实际政治三年之久。他谦卑地延揽专家，曾亲自拜访过孔丘，邀请孔丘任职，但孔丘怀疑他政权的稳定性，没有接受。”<br />
　　历史记载是：孔子似乎有点动心（至少子路这么认为），但是阳虎这种臣子的臣子出来主政，毕竟不符合孔子的理论；所以子路力阻，孔子也就没去。跟稳定性是无关的，至少完全无证据可言。<br />
<br />
　　第九章，纪元前第五世纪。<br />
　　在本章，柏杨继续恶毒攻击孔子。当然，孔子也不是没毛病。象杀少正卯这种事，虽然朱熹认为并无此事，但是此前儒家认可已久；被柏杨拿来当信史来骂，孔子泉下有知，也只能找荀子算账，还算不到柏杨头上。（这事似乎都是从荀子那儿来的）<br />
　　但是，也有骂得不沾边的，柏杨写道：“《诗经》出现于纪元前十二世纪，是中国最早的一部诗歌选集，包括贵族作品和平民作品。孔丘把它作一次重大规定，依孔丘的标准，只保存了他认为有价值的三百首，其他的都被淘汰――这是中华文化最大的损失。”<br />
　　人家编订诗集（假设是孔子编的）必须全部保留，不许淘汰？要真是全部保留，弄得太长，没人看，等于全部淘汰了。照这个逻辑，所有编订唐诗宋词几百首的人，都是造成文化损失的人了。<br />
　　而且，还有不是骂，但也对不上号的。孔子云：“麟也，仁兽也，出而死，吾道穷矣。”柏杨偏偏写成：“孔丘叹息说：‘古人有言，世界和平，上有圣明的君王，麒麟才会出现。现在世界大乱，它却出现了，真是怪事，我的智慧已经干枯。’”<br />
　　真是怪事。<br />
<br />
　　然后，柏杨还要无端赞美墨子：“为了拯救他的第二祖国，他从鲁国首府曲阜（山东曲阜）狂奔十昼夜，赶到楚王国首都若阝城（湖北钟祥西北人曲阜到若阝城航空距离六百一十公里，千山万水，盘旋弯曲，即以最低限度两倍计算，也有一千四百公里，只走了十天（那时代还没有马匹，全靠徒步），可看出支持他拼命奔跑的爱心。”<br />
　　姑且不论柏杨考证这种未必是实数的数字是否有意义，“那时代还没有马匹，全靠徒步”的话也亏他想得出来。估计千秋万代以后，某人赞美柏杨“狂写十昼夜...支持他拼命写作的爱心”的时候，也会说“那时代还没有脑子，全靠徒手”吧。</div>

							
							
							
							
