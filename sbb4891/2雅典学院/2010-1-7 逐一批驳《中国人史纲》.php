　　更何况这类君不君臣不臣的原始共产主义倾向，于儒家何利？只不过人们都这么传说，儒家也不能分辨，误以为真罢了。儒家要真是“为了政治上的目的”篡改历史，早把这些玩意儿改没了。</div>

							
							
							
							
