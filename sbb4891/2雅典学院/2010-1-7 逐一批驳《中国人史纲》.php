																											<div id="postmessage_419767" class="t_msgfont">　　柏杨其人行文尖酸刻薄，本文就要让他自食其果。要知道，跟不讲理的人讲理，不但不可能有用，反而被笑为迂腐。只有让他自食其果，方为正道。<br />
　　逐一批驳，连续更新。<br />
<br />
　　前三章分别是：历史舞台、神话时代、传说时代。所谓历史舞台者，地理也，这东西一般都无所谓不对。“神话传说”则是认不得真的，你跟他认真就输了。所以就都不论了吧。<br />
　　倒是第一章有这么一句：“现在，让我们搭乘可以跟光速相等速度的太空船，从广阔的太平洋向西飞航。”体现作者的文傻本色。不懂理科倒无所谓，你说点什么“刹那”“一瞬”“疾速”什么的，谁也不会把你当傻子卖了。唯独看了“光速”这个词时髦（时髦就时髦在它是物理学中的速度极限），拿来就瞎用，可谓“文傻”。<br />
　　柏杨其人，就是“文傻”加“左傻”，双傻临门。下面慢慢分剥哦。<br />
<br />
　　第四章，半信史。<br />
　　夏朝基本子虚乌有，到了柏杨这里说得有鼻子有眼儿，稿费真是好赚。当然，话说回来了，柏杨倒是写了“半信史”这三个字，所以除了涉嫌故意拖戏以外，倒也没什么大错。<br />
　　无独有偶，到了纪元十二世纪，伊里亚特的传说也拿来拖戏......<br />
　　好吧，进入正题，周王朝建立了，姬昌姬发姬旦，都还算熟悉吧。但是...怎么这么别扭呢！对了！“文武周公”这些词跑到哪儿去了？原来，柏杨这厮满怀着无产阶级革命家对古代封建统治者的深仇大恨，把人家的尊称都给去掉了。姬昌姬发姬旦还算勉强熟悉吧，接下来的名字可就一个比一个不熟喽。<br />
　　说起来，当年有个家伙为了讨好皇帝，给乾隆上书说：某县的名字乃是明朝的年号，应该改了。结果被皇帝驳回：名字这东西要是瞎改，以后岂不是要谁也不认识谁了？夷狄之心，尚且如此；柏杨这个激进分子，连夷狄都不如呢！倒有这么一句话对他挺合适：“外中华而内夷狄”（外者，外貌也）看起来是文明之邦的国人，心里却是凶胡狡竖（比方说拿卢布的，或者丧了家的俄夷走狗）。</div>

							
							
							
							
