																											<div id="postmessage_416037" class="t_msgfont">阿凡达：战死在自家门前的台阶上》<br />
　在《飞屋环游记》之后，我又和朋友一起去看了《阿凡达》――把它们连在一起是因为两个片子都是在谈拆迁。 <br />
　　我非常关注拆迁问题，所以我只为这两部片子买单进了影院（连2012都没做到）。 <br />
　　美国人很好的瞄准了我的钱包，他们连着弄了两个拆迁大片。上一个只是让我觉得温馨，而这一个，几乎让我落泪。 <br />
　　 <br />
　　为什么一个美国政治家演讲的时候会产生煽动力――即使不懂英文你也可以感受到？ <br />
　　除去其个人魅力原因。是因为资本主义的政治经济学本身就包含了一种激情。 <br />
　　这种激情建立在对私有财产保护的基础之上。这种激情，是美国立国的基础。个人的资产神圣不可侵犯――换句话说：这块土地是我的，代表使用权和所有权全是我的。不是政府租给我的，没有70年的使用期限。我可以传给我的儿子，孙子，子子孙孙无穷匮也。没有得到我的允许谁也不能进来。强行进入不听警告我可以开枪射箭，杀死为止。即使你是政府，即使你是党。 <br />
　　 <br />
　　“一个农场主，为了捍卫自己的土地，要有战死在自家门前台阶上的激情。” <br />
　　 <br />
　　这是美国的政治经济学的激情的本源。奥巴马站在演讲台上，他知道自己是这块土地，这个国家的主人，他说什么话，底气都很足。所以美国人可以跑到全世界甚至外星球去，操着一口得意洋洋的美国英语。 <br />
　　 <br />
　　这也是阿凡达打动我的地方。为了保卫自己的土地，我们要视死如归。就像纳威人，像上海那个和政府动迁队对打的女士。即使一边是热兵器，一边是冷兵器，一边是武装警察，一边只有土制燃烧瓶。 <br />
　　 <br />
　　读过中国近代史的人都会明白热兵器与冷兵器对打的悲壮情景。你会想起一个叫“螳臂当车”的成语，但你一定是在为螳螂们哭泣。热兵器的出现，代表着一个时代的过去。英国人派了几千名远征军几座大炮就搞定了我们，不是我们一下子变得积贫积弱，也不是清朝皇帝不够好，也不是因为满洲人太蛮夷搞坏了中国，也不是林则徐激化了矛盾，也不是我们的文化活该完蛋。都不是，这叫历史的玩笑。这是某些莫名其妙的高级生物在搞“生态平衡”。 <br />
　　 <br />
　　胡林翼灭太平军的时候打得正开心，看到洋人的军舰会马上吐血。这不是他身体不好，这是因为除了吐血你什么都做不了。 <br />
　　 <br />
　　冷兵器包含了一部分的威慑作用，因为其往往致伤而不致死。热兵器致死率太高，消灭有生力量的能力太强大了。知道不能打，还得打，这是悲剧的力量。所以当纳威人全国总动员的时候，我几乎哭了。因为那个瞬间我回到了1840年，变成了那个接到战报之后心底发凉的道光皇帝。 <br />
　　 <br />
　　卡梅隆想必并没有要通过此片为西方的殖民侵略赎罪的意思。但在我看来，这个片子实在是重现了当年西方靠着热兵器去灭冷兵器民族的过程。我们确实曾是个前现代种族，但我们感通天地，如同纳威人，当年修了铁路，有人说这动摇山川，恐遭天谴，现在很多人把这个当做我们蒙昧的证据。说这话的人，实在是不懂得我们曾有过什么样的祖先。我们现代化搞了这么多年，搞得鸡飞狗跳，所有人都想洗了牌重新在来，TMD在上海坐个出租车，司机都想重新推倒重来。150年，重来了这么多次，本质上仍和古代相去未远。我们学西方学得完全走样，自己的传统也弄得乱七八糟。然后大家守在互联网上，一天到晚骂D不好。这是什么，这是无耻。和东欧不同，49年，我们是欢天喜地的一手培育、接受了这个D。现在有人说，那是因为当时他标榜民主，把我们骗了。这是错的。这是我们“自个一步步，一步一步，走到这田地来的。” <br />
　　 <br />
　　为什么我们国家的各级领导讲话的时候没有一丁点煽动力――即使你懂中文也感受不到哪怕一点？ <br />
　　除去个人原因。是因为我们的国家建立在了一种错误的激情之上。 <br />
　　这种激情从古到今一直存在。答曰一个字：“抢”。 <br />
　　他曾有很多名字：“王侯将相宁有种乎”“均贫富”“全世界无产者联合起来”“打土豪分田地” <br />
　　准确点的表达是：我们的政治经济学所包含的激情是建立在对他人的财产的向往之上的。 <br />
　　这种激情来的更爽，因为更原始，因为攻击他人永远比建构自己来的轻松。 <br />
　　如今的世界，这样的情绪不上台面，我们的领导在讲话的时候不好表现出来，否则会像当众表演高潮有损国格。所以只好忍忍忍，然后说一些屁话。 <br />
　　 <br />
　　所以，49年发生的事情，其实正是迎合了每个人心底深处的这种破坏的激情。杀了那么多地富反坏右，土地也分了，也做了主人了，大家都穷了，也没人有财产好给我们向往了，所以我们彻底玩完了。现在回过头来，我们发现自己已经落后的厉害了，世界各国似乎都不好欺负了，于是只好回过头来骂我们当初选择的D。就像当初玩过的女人，爽过了，回过头来骂她跟自己不一条心。其实他起初不过是我们每个人心中的一条狗，生生被我们惯成了怪兽。所以大家都别骂了。好好自我反省一下，以后见了别人有钱不要动辄就想去均分之，说什么公平公平要杀了所有有钱人。只有自己的心变了，这个社会才会从根本上发生改变。 <br />
　　 <br />
　　《阿凡达》所表现的这两种激情的斗争是自始至终的。虽然影片最后让纳威人获得了胜利，但我想所有人都知道，真实的历史中，这一切并没有发生。中国、非洲、拉美，都有很多野生动物，但没有子弹打不死的。这很遗憾。 <br />
　　 <br />
　　悲情的是现在的中国完全被我们搞坏了，还不如潘多拉。潘多拉的居民拥有自己的土地，可以做地主。我们49年打土豪分来的田地没几年就被养大的小怪兽骗走了。我们只能落寞的站在淡红色的天际之下，羽扇纶巾，摸着秃头，说自己此生命犯天煞孤星，注定孤苦漂泊只是过客。 <br />
　　 <br />
　　如果有一天――我是说如果，小怪兽可以把我房产证上那句“使用期限70年”去掉，再有外星人入侵的时候，我会第一个参军，并战死在自家的台阶上。因为那时我非常清楚，这块地TMD是我的了，这块地上的每个土坷垃上，都打上了“王”字的LOGO（注意，既不是中华人民共和国，也不是上海），如此这般，我才愿意为它而生，为它而死。没别的，保住了这块地，我和我的子孙后代，就不再是浮萍。 <br />
　　 <br />
