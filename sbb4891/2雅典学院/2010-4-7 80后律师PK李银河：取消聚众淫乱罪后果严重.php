																											<div id="postmessage_552213" class="t_msgfont">80后律师PK李银河：取消聚众淫乱罪后果严重<br />
2010年04月07日 07:21来源:红网刘灿 李卫林<br />
80后律师方文斌PK李银河<br />
<br />
<br />
　　红网长沙4月6日讯（见习记者 刘灿 记者 李卫林）近日，南京教授换偶事件成为社会各界关注的焦点，网友对此展开了激烈讨论，不少声音质疑当事人马副教授等人的行为不构成犯罪。中国著名社会学家、性学研究者李银河则表示，换偶是公民个人自由权的表现，受宪法的保护，并呼吁修改现行《刑法》。<br />
　　<br />
　　4月5日，湖南人和人律师事务所80后律师方文斌在红网《百姓呼声》栏目发表文章，以律师的角度对南京教授换偶事件做出解读，对李银河等人的观点进行了批驳。“我希望以一名律师身份表达看法，也算是顺道与李银河等人进行一次说道。”<br />
　　<br />
　　聚众淫乱可能面临刑事处罚<br />
　　<br />
　　“针对南京教授换偶事件，有法律人士认为马某等人的淫乱行为是在私人场所进行的，没有社会危害性，不应当受刑事处罚。本人认为这种说法没有任何法律依据，是对法治尊严的侮辱。”针对有律师称马某等人的行为不构成该罪的说法，方律师在文章开头就指出，聚众淫乱可能要面临刑事处罚。<br />
　　<br />
　　方律师认为，如果马某等人的行为经过人民法院审理事实成立的话，即构成《刑法》第三百零一条规定的聚众淫乱罪，将面临刑事处罚。“聚众进行淫乱活动的，对首要分子或者多次参加的，处五年以下有期徒刑、拘役或者管制。”<br />
　　<br />
　　“由此可见，聚众淫乱的行为是否发生在私人场所并不是认定罪与非罪的构成要素。只要有聚众淫乱的行为，并且是首要分子或多次参加的，就构成犯罪，而不论行为的发生地是公共场合还是私人处所。”方律师表示，马某等人的行为具有极大的社会危害性。“有人指出，马某等人的行为都是自愿的，没有谁强迫谁，没有受害者，怎么就是犯罪呢。本人认为这是故意或真正的无知。”<br />
　　<br />
　　“任何犯罪的首要特征便是其社会危害性，即行为对刑法所保护的社会关系具有侵犯性。《刑法》第十三条规定来看，只要行为侵害了法益，便具有了社会危害性，而不是说有受害人就有社会危害性，反之则无。”<br />
　　<br />
　　“聚众淫乱罪在《刑法》体系中位于分则第六章第一节，马某等人的行为属于妨害社会管理秩序罪中的扰乱公共秩序罪。”方律师认为，聚众淫乱行为是严重扰乱公共秩序的行为，侵害的是国家的社会管理秩序。侵害社会管理秩序，是对社会法益的侵犯，也是对每个社会成员法益的隐性侵害，是一种具有社会危害性并受刑法打击的行为。<br />
　　<br />
　　打击聚众淫乱行为不侵害公民权利和自由<br />
　　<br />
　　对于李银河在回答《南都周刊》记者提问时指出的“刑法对该种行为以犯罪论是对公民个人权利与自由的侵犯，与宪法保护的公民人身自由权相矛盾以及北京林业大学性学研究者方刚也认为换偶是一个应该尊重的个人选择”的说法，方律师认为权利与自由的一个隐含命题，打击聚众淫乱行为并不真正侵害公民权利和自由。<br />
　　<br />
　　“换偶行为，尤其是像马某等人这种超大规模的聚众换偶行为，是一种以权利与自由为幌子的个人恣意，不但得不到法律的保护，而且是应受到法律制裁的违法犯罪行为。”<br />
　　<br />
　　方律师表示，权利与自由的一个隐含命题是“这种公民个人可以自由支配自己行为是正当的，受法律保护，受社会其他成员的尊重。当这种自由的行为不正当，受法律摒弃，则不能称之为权利与自由。而当公民个人在实现自己权利与自由的过程中损害到他人也应当享有的权利与自由时，权利行为则质变成了侵权行为。”<br />
　　<br />
　　“人类社会里的公民，权利在实现过程中都会有交叉，如果这种交叉得不到规范和普遍遵守，则会出现混乱，权利被权利剥夺，自由被自由褫夺。人类社会从来没有绝对的自由，绝对的自由导致绝对的不自由。”方律师认为，“当换偶行为达到聚众淫乱时对行为人课以刑罚，以达到对社会管理秩序的终极保护，从而实现法律对全体社会成员的权利保护。”<br />
　　<br />
　　换偶不仅仅是道德问题<br />
　　<br />
　　对于李银河一再强调的“聚众换偶仅仅是道德问题，这一权利不应当以违反道德或违反习俗的名义被剥夺”的说法，方律师表示不能用“道德问题”当挡箭牌，良好的道德、社会的公序良俗应该得到全体民众的尊重。<br />
　　<br />
　　“李银河认为‘马某等人最主要的冤屈在于他们没有真正伤害任何人，他们所伤害的只是社会习俗，但是不能因为一个人违反社会习俗就把他抓起来、开除公职，甚至起诉’，我觉得不仅是道德和社会习俗问题，而是严重影响社会稳定与发展的犯罪行为。”方律师认为马某等人聚众换偶的行为不仅是道德问题，而是触犯聚众淫乱罪是典型的犯罪行为。<br />
　　<br />
　　“本人引用一句法律名言来说明本人的观点：法律是显露的道德，道德是隐藏的法律。”方律师表示，道德、公序良俗是人类社会文明的积累，是人类社会人际交往中最值得人们所珍惜和尊重的社会规范，在社会调控功能上发挥着以弥补法律调整空白的重要作用，如恋爱关系、情感冲突等。“良好的道德，社会的公序良俗，应当得到我们全体成员的尊重。”<br />
　　<br />
　　聚众淫乱罪取消后果严重<br />
　　<br />
　　对于李银河提议取消聚众淫乱罪以及其取消理由“在最近二十年内，已经没有人再因为这个罪名判罪了。所以，它已经是一个死掉的法律了”的说法，方律师也不赞同。<br />
　　<br />
　　“首先，《刑法》第三百零一条不是一条‘死掉’的法律；其次，取消该罪的重大意义不是防止权利被践踏，而是解决性泛滥的法律制裁后顾之忧。”方律师认为，不能因为先前少有处理，后发生的行为就不能被处理，更不能因此而否认据以处理该类案件法律的存在合理性。<br />
　　<br />
　　“这不符合基本的逻辑推理，更不符合法律的一般原理。”相反，方律师认为，对于聚众淫乱罪不应被废除，还应该得到进一步的加强。“对这种严重损害善良道德和公序良俗、触犯刑法的行为予以公正的处理，将使恣意的个人自由得到理性规制，社会的法益得到维护，全体社会成员的权利才能得到更为安全的保护，法治才会进步，文明才会发展。”<br />
　　<br />
　　方律师在文章的最后写道：“法律不保护权利的沉默者，如权利受损，请奋起维权；同时，法律也不保护权利的恣意者，行使权利请不要损害他人和社会的权利。”<br />
对话方文斌：为什么要和李银河等人说“道”<br />
　　<br />
　　这篇题为《南京教授换偶事件的几点思考――顺道与李银河先生说》的帖子4月5日在红网《百姓呼声》栏目发表后，引起了网友争论，许多网友跟帖留言，有支持方文斌的，也有“骂”方文斌是“满口仁义道德的伪君子”。<br />
　　<br />
　　4月6日，记者采访了高调PK李银河、《南京教授换偶事件的几点思考――顺道与李银河先生说》作者、湖南人和人律师事务所律师方文斌，在接受记者采访的同时，方文斌对网友的质疑提出了自己的看法。<br />
　　<br />
　　记者：为什么要在这个时候选择这个话题发帖？<br />
　　<br />
　　方文斌：聚众淫乱，这是违反我国《治安管理处罚法》，甚至是触犯《刑法》的行为。法律是最低限度的道德，这种行为在道德上的侵害性就更不用说了。但我想不到这种行为竟然得到了部分人的追捧，其中还有社会精英。有感于个人权利与自由观念的高度膨胀，竟到了如此极不道德以为道德，严重违法以为不违法的地步。李银河先生说要为了马教授的权利呼吁，作为一个律师，我想为了法律的尊严呼吁，为了文明呐喊，为了每个公民应负的社会责任与义务奋起一书。<br />
　　<br />
　　记者：对于你的观点，网友好像“骂”你的比较多。<br />
　　<br />
　　方文斌：对于骂我的网友，我觉得一是比较遗憾，二是不想应对他们。但还是想说一句：过度的欲望是一切罪恶的来源。<br />
　　<br />
　　记者：网友封你为“封建卫士”，你怎么看？<br />
　　<br />
　　方文斌：我很高兴我能成为一个社会的卫士，但我不认为现代的美好道德和公序良俗与过去的社会所具有的道德传统有联系和继承关系就成了一个遭遗弃的“封建思想”，而我怀有对社会公平和善良的追求就成了个“封建卫士”。<br />
　　<br />
　　记者：对李银河的印象是怎样的？<br />
　　<br />
　　方文斌：我对李银河的印象起源于王小波先生，我从中学时代开始阅读王先生的著作，很欣赏王先生。后来得知李银河是王先生爱人，也是位研究性学的专家，观点前卫，作风果敢，对她我也一直很尊敬。但此次我看到她的这个言论，觉得她的研究和观点，至少是关于聚众换偶这个事件的观点，是没有社会责任的。<br />
　　<br />
　　记者：欢不欢迎李银河回应？<br />
　　<br />
　　方文斌：我欢迎李银河先生的回应，我期待我们能从权利与自由到道德与法律，从学术研究到社会责任，进行一次深入的探讨</div>

							
							
							
							
