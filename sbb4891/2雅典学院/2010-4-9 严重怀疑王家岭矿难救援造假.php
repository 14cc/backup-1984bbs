													<h2>王家岭煤矿救援的疑点</h2>
						
						
																											<div id="postmessage_556850" class="t_msgfont">以下文字转载自：<a href="http://zhaomu.blog.sohu.com/148002565.html" target="_blank">http://zhaomu.blog.sohu.com/148002565.html</a><br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;先说说我的身份，本人煤矿工作20余年，从事煤矿救护工作十来年，曾任煤矿救护队技术副中队长，多次参与或带队处理煤矿井下事故，有在综采工作面氧气浓度仅3.8%的灾区佩用四小时氧气呼吸器工作一个多小时的救灾灭火经验，大学文化，高级工程师。<br />
　　　　1、关于钻机钻孔的技术问题。大口径钻机(打的钻孔钻杆口径是210mm)参与救援其实有许多难题，如终孔点的定位问题，把一个几百米的钻孔的终孔点精确地定位在一个宽度不超过3米的巷道上，其钻孔点的精确确定是难度极大的，王家岭煤矿距地表垂直距离最近的巷道是回风巷，270m，而且其人员位置并不一定在井口附近，更增加了钻孔定位的难度。而且，在井下环境及其恶劣的情况下，矿工能找到钻头装置并系东西在上面，一个数百米的钻孔，钻孔深度也并不怎么确定的情况下，钻头不偏不斜不高不低正好打在巷道里，而且能让矿工系东西，是不是有点天方夜谭的味道？<br />
　　　　关于钻孔救援问题，若干年前俄罗斯搞过大口径钻孔，那是专用的钻机，是通过钻孔向井下输入氮气灭火，钻孔定位当然是难题，钻杆钢度不够或钻杆直径不够大，那钻杆会象面条一样偏移终孔。而且王家岭煤矿的地质资料是否准确齐备也是个问题，因为那钻孔的准确定位是以准确的标高和经纬度为前提的，没有这些一切都是空谈。<br />
　　　　有人说只要有准确的图纸 不是什么难事，但王家岭煤矿的打钻孔救援此前的前期准备工作并没有详细的报道，如地质资料是否详实准确，地质部门打钻孔位置的确定和测算经过等，这些都需做细致的工作，王家岭煤矿的地质资料有是否详实齐备，新闻媒体没报道，大家也不知道。<br />
　　　　我是搞工程技术的，违反国内技术条件和科技水平的说法本身就令人怀疑，因为救灾情况的真实性关涉公共安全，涉及到公共利益和公民财产和人身安全，追求真实归根结底是人类追求自身安全的需要，决不允许违反常识和国内的技术条件搞虚假报道，也决不允许有人利用救灾搞政绩工程，2008年四川地震中出现了所谓9岁的林浩带伤救人(3人)的报道，后被全国很多人证实是虚假报道，因为一个无民事行为能力的9岁的林浩救人是违反常识的，让这样的人在地震时带伤救人几乎比登天还难，林浩救人被证实是虚假报道后现在也不了了之了，新闻单位从来不对其报道的真实性负责，从来不向民众道歉，因为自己揭穿自己的谎言自己打自己的嘴吧子比什么都难，况且这样的谎言的揭穿也没那么简单，因为它涉及一种行政法上的国家行为，那ＣＣＴＶ本身就是国家办的，它从来不允许有第二方，第三方声音的人员在媒体讨论和怀疑9岁的林浩救人的真实性，它从来都是一个调子，一个声音，听不到第二方、第三方声音，听不到第二方、第三方声音的新闻不是新闻，充其量是一种宣传。<br />
　　　　报道中的“王家岭矿难发生120小时后发现井下有生命迹象，救援人员通过2号孔道送下去360袋营养液，还捎下去两封信和笔、纸、电话，至截稿时，地面仍在焦急地等待着井下的回音”这种说法的真实性也急切证实。因为国内煤矿工程技术，目前还没有达到这种钻孔定位送水送信的技术水平，那几台钻机是什么型式的钻机，是不是专门用于救灾的钻机，现在从报道方面分析从来没有说是专门用于救灾的钻机，不是经过特殊研制和制造的用于救灾的专用钻机能“送下去360袋营养液，还捎下去两封信和笔、纸、电话”我做为一个矿山老工程技术人员对此种说法严重怀疑，因为根据我的跟综掌握国内的救灾技术目前还没有达到这种水平，更不用说一般的钻机了，此前国内也没有类似的成果或救灾实例。<br />
　　　　请大家注意，王家岭煤矿井下巷道距地表最近的垂直距离是回风巷，为270m，打这样的钻孔可以称为长距离钻孔了，请大家注意，我是搞煤矿工程技术的，若干年前我国在煤矿搞大口径1000m超前钻孔的科研项目，被称为具有世界前沿水平，而且这样的具有国际前沿水平的钻孔其钻孔开口口径是500mm的口径，也就是说钻孔的口径是不一样的，越深或距离越长口径越小，钻孔的终孔口径已经相当小了，当然那种钻孔是水平钻孔，难度更高。王家岭煤矿所用钻机的钻杆直径是210mm其实巷道定位等也存在诸多难题，它们用的不是专用钻机，可他们竟然搞成了，而且还“送360袋营养液，还捎下去两封信和笔、纸、电话”，我是严重怀疑其真实性。<br />
　　　　我感觉，油田钻井并不存在定位问题，因为那达到要求的范围很大，那是向一个所谓油田的大范围内打钻孔，而且终孔也并没有太大的要求，只要深度够，按一定的方式方法排列，打到油层或煤气层，即使深一点也没什么关系。大家可以讨论一下。<br />
　　　　2、这是一起透水事故，而且时间已过了若干天，井下极其潮湿和脏乱，衣服和手碰到哪都是泥，但大家看到没有？那些直接从井下抬着矿工从回风井出来救援的人员的衣服有多么光鲜，多么干净？这符合常理吗？是不是POSE“摆拍”出来的？<br />
　　　　大家注意一下，救护队员穿的是服装叫战斗服，他们从回风井出来那战斗服确实很光鲜，回风井，即使让那污浊的风流过一下，战斗服都很脏。<br />
　　　　并且，大家知道吗？在井下用担架抬人，四个人，那是很累人的，一会就汗流满面，但大家看到的那些救护队员从回风井出来时，不仅战斗服很干净，而且并没有出多少汗。<br />
　　　　我曾经多次参加井下救人抬人工作，我们一般是让井下工作单位现场的矿工抬，因为一个小队只有九人，如果再做其他的工作，人手是不够用的，所以我们会借助现场单位人员的力量，抬人由他们抬，到地面后再换过来，这抬人的工作相当的累人，没做过此工作的人是不知道的。<br />
　　　　这句话我补充一句，在王家岭煤矿的救援中，这种担架谁帮他们抬呢？这种帮他们抬的人是绝无仅有的，只有他们自己动手，自己动手衣服不脏，没流多少汗，没有累得气喘嘘嘘身体发软，是很奇怪的事情。<br />
　　　　而且王家岭煤矿是斜井，当时回风井出来的人是斜井人车把他们一起拉上来的也不现实，因为，当时井下并没有恢复用电，而且很重要的一点，那是回风井，在煤矿中，回风井做人员升入井井筒是不允许的，回入井筒甚至不允许大量的电源及电器机械设备的存在。<br />
　　　　大家注意一下，那救护队员身上背的东西叫氧气呼吸器，以前是负压的，现在大多数煤矿的救护队员都改用正压氧气呼吸器了，他们身上背的东西我是相当的熟悉的，若干年前我们国内刚引进的时候，我在重庆参加救护年会的时候还听过德国专家的介绍，当时一台德国进口的是一万五千美元，现在国内仿制的厂家已多如牛毛了。大家注意了吗？他们救援后的氧气呼吸器也很干净，这就很奇怪的，背着那东西救护不碰东西是不可能的，而且是透水事故，那氧气呼吸器碰到哪都会沾上呢，但大家看一看，那些氧气呼吸器都很光鲜，甚至象没有下过井的样子。<br />
　　　　再有，这里告诉大家，背着氧气呼吸器抬人，那劳动强度绝对是高强度的活，几乎类如快速地爬山，一会你就会受不了，我在这里给大家一个参考数据，一个人小跑的体力输出相当于中等劳动强度，那背着氧气呼吸器抬人的活绝对是比中等劳动强度高两个等级的体力输出，是超高的劳动强度，但看那些队员却很轻松的样子，一是流汗不多，战斗服不脏，身上背的氧气呼吸器没有泥，并没有想象中的那样劳动强度的样子。<br />
　　　　3、更令人怀疑的是，矿工的生理问题，长胡子是矿工生理现象，谁也抗拒不了，有人一天不刮胡子就受不了，几天不刮感觉就成了“黑李逵”，可是王家岭煤矿救援上来的矿工照片和视频显示却象胡子刚刮过一样，也就是说这些矿工经过了将近十天的时间没长胡子？自然规律被这些矿工打破了，真有这样的奇迹？<br />
　　　　请大家注意，那些矿工可是直接从井下，而且是从回风井抬上来的，并没有经过医院医务人员的擦洗和刮脸，没长胡子也着实令人怀疑其违反生理现象，违反自然规律，一个没长，二个没长，若都没长就更是明显违反常识和自然规律的不实现象。这些都要有第三方更多佐料的证实，谎言真不了，前苏联《真理报》只有一家真理除日期是真的没有一句可信的时代毕竟过去了，但它的影子还在，所以我们要在理有据地怀疑，小心地求证。<br />
