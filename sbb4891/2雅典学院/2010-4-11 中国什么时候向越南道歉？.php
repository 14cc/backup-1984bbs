													<h2>中国什么时候向越南道歉？</h2>
						
						
																											<div id="postmessage_558563" class="t_msgfont">新中国建国来虽然身处东方阵营，但从来都将外交政策视同儿戏，对国际规则也是嗤之以鼻，直弄得和周围邻居全部翻脸，全球国家都成了敌人，最后不得不厚着脸皮同“头号资本主义国家”美国，进行所谓的“乒乓外交”。当时中国的国家性格就是暴戾、极端、自私、还有些神经质。<br />
<br />
中越战争，全世界东西阵营基本上都是站在越南一方，谴责中国，好像只有两个国家支持中国（柬埔寨、朝鲜），具体原因有内因也有外因，内因一是当时文革刚刚结束，上台不久的邓在文革中几次被批倒批臭，远离政治核心，因为个人原因需要重新竖立个人权威。第二文革时国内大乱，国家领导瞬间变成汉奸特务的事情屡见不鲜，极大地影响到国内人民统治阶层的信任危机，所以，领导层也需要这场战争凝聚民众的向心力。第三，经过文革的混乱时期，考察解放军战斗实力。外因，一是解救红色高棉（这是最重要的）。迫使越军主力从柬埔寨撤离。第二是警告苏联，企图遏制苏联在东南亚的实力扩张，第三是条件许可，苏联正在阿富汗焦头烂额中，无暇顾及越南局势，而中美已经建交，况且，对于红色国家之间的战争，美国从无兴趣。<br />
<br />
至于国内媒体反复强调的排华、边境骚扰只是借口，如同日本炮制的卢沟桥事变。<br />
<br />
中越战争，中国并不以侵占对方领土为目的，除了帮助血腥的红色高棉外，赤裸裸的展示大国沙文主义，特别是战争后期，几大军区轮以练兵为目的轮流参战，更是将越南这个主权国家当作自己的练兵场，美名其曰，锻炼队伍，这不是霸权是什么？<br />
<br />
中越战争，中方目的并未达到，具体伤亡人数也一直秘而不宣，这些人和朝鲜战争死亡的士兵一样，同样被冠之以“为国捐躯”“烈士”“人民卫士”等等称号，就地掩埋，一个个鲜活的生命，就这样毫无意义的为了“国家”捐献了出去，留下的是一个个破损的家庭和失去孩子的父母。同样的，对于伤亡更大的越南，我们不但没有丝毫反省和道歉，至今还强词夺理的抹黑人家，我们和日本右翼又有什么区别？<br />
<br />
中越战争，我们号称“自卫反击”，深入越南腹地，在出境作战短短的十几天内，犯下无数罪行，随便举几个，老街战役使用毒气，致使坑道内一千多人死亡（士兵仅仅200），部队撤退时实行“三光政策”，目的是使越南经济倒退几十年，所有桥梁全部炸毁，房屋烧毁，井内下毒，甚至连个鸡窝都不放过，引起国际关注的还有一事，一辆坦克被越南小孩炸毁，部队四处寻找，将这群孩子（十几个）抓住后全部活埋，这些孩子最大的只有十五岁，此事经过大致如此，战后还被告到国际法庭。不止在境外，也曾发生过士兵侮辱国内傣族妇女的事情。以上据这些例子并不是想彻底否定中国部队，我想说的是，在战争状态下，任何国家的军队都差别不大。有些朋友通过日本军队在中国犯下的罪行来彻底否定这个民族，完全没有必要，如果将中国部队放到越南八、九年时间，我估计和日军也差不到多少。<br />
<br />
国内媒体对内宣传主题全是“中国军人，以血肉之躯捍卫了祖国的尊严”，在我看来，应该是“中国军人，以血肉之躯，践踏了了越南的尊严”，中越战争严重抹黑中国的国际想象（当然，中国的领导人也从没有在乎过），使南亚国家对于中国始终保持警惕。这场战争主要目的是支持血腥的红色高棉，从这个角度看，这是一场完全的没有道义而言的战争。<br />
<br />
９９年，老山、者阴山、法卡山相继交还越南政府，这说明，这些我们曾经为之自豪的领土，本来就是越南的。那么，我们除了献出上万条鲜活的生命外，还得到了什么？</div>

							
							
							
							
