													<h2>房价随想</h2>
						
						
																											<div id="postmessage_427675" class="t_msgfont">以前我一直犯个错误，就是用房价收入比来衡量中国一线城市的房价是否存在泡沫。事实上，房价收入比这种国际标准到中国严重水土不服，国际惯例在中国国情面前往往要俯首称臣。中国不是市场经济，是政府主导的经济。党是领导一切的。房价降不降，关键还是要看党的意思。<br />
<br />
&nbsp; &nbsp; 中国一线城市的房价跟当地人的平均收入根本没多大关系。因为来一线城市买房的绝大多数都不是本地人。2009年上海的新房价格涨了一倍，其中只有10%是上海本地人买的。其余都被江浙一代的人（温州炒房团）和不差钱的山西煤老板给买走了。上海本地人有多少能买得起外环以内的商品房呢？同样，在深圳09年买房的主力是香港人。中国富人那么多，山西煤老板，温州炒房团，还有高级公务员和国企高管，可一线城市就北上广深杭几个。他们都拥到那里买房，一线城市的房价能不高吗？<br />
<br />
&nbsp; &nbsp; 最近说要对第二套房征物业税了。对此我表示反对。国外对第二套房征物业税是因为国外买房就有了永久产权，想住多久住多久，可以当祖产传给后代，不用怕有人强拆你的房子。可中国的房子只有七十年使用权，在中国买房说到底只是一次性把七十年的租金缴清而已。还要祈祷你不碰到强制拆迁，买的房子质量能撑七十年，实际上中国绝大多数的房子建筑质量只能撑三十年，而且越是新的房子质量越是差，开发商普遍偷工减料。政府想在物业税上与国际接轨，在强制拆迁上玩中国国情，天底下哪有那么好的事？<br />
<br />
&nbsp; &nbsp; 另外，征物业税只是打击炒房者而已，对高房价的打击着实有限。房地产有四大利益集团，拿土地出让金和房地产相关税收的地方政府，给开发商和买房者放贷款赚利息的银行，造房子的开发商，还有就是炒房者了。征收物业税，打击的是四大利益集团中的炒房者。四大利益集团中，地方政府当然是官方力量了，中国的银行全是国有，也属于官方力量，09年房地产业严重国进民退，地王大都是拿到巨额信贷的国企拿。王石和潘石屹这种民营房地产商在一线城市都拿不到什么地，只能去二线城市拿地了。说明房地产商也有像官方发展的趋势，征物业税，打击的主要是炒房团这种民间力量。而官方房地产既得利益集团没有什么损失。最后会造成官肥民瘦。<br />
<br />
&nbsp; &nbsp; 不管要征什么税，都要经过纳税人的同意，否则就和抢劫没区别了。炒房团也是人，也有权利。如果有人说征物业税虽然程序上有问题，但是大快人心，那么把任志强活剐了肯定还要大快人心。我觉得还是要遵守程序正义。政府这次可以未经纳税人同意开征物业税打击富人，下次就可以再征个什么税打击穷人。<br />
<br />
&nbsp; &nbsp; 中国的房价高，很大程度上是地方政府与民争利的结果。中国政府好像永远喂不饱，不管拿了多少好处都要哭穷。政府有钱搞神七，办奥运会，世博会，就是没钱解决国民的医疗住房教育等民生问题。现在房价中流向地方政府的土地出让金和房地产相关税收要超过50%，加上银行得到好处，政府绝对是高房价最大的受益者。现在虽然出台很多调控措施，但地价还在涨，地价和房价就像面粉和面包的关系，地价在涨，房价怎么可能跌？你们见过面粉涨价面包跌价吗？<br />
<br />
&nbsp; &nbsp; 严格地说，不是房价在涨，是人民币对房价的购买力在下降。2001年黄金价格270美元一盎司，现在1100多美元一盎司，涨了四倍多，01年上海的房价3000元一平方，现在16000一平方，涨了五倍多。01年上海公交车票五毛，现在两块，涨了四倍。纸币的购买力一直在下降。国民收入增长的速度赶不上通货膨胀的速度。我们的货币政策和美国捆绑，美国开印钞机，我们也只好开印钞机，开印钞机就是变相加税，最后倒霉的还是老百姓。<br />
<br />
&nbsp; &nbsp; 08年的房价下跌是因为紧缩货币政策导致开发商资金链紧张，09年房价量价齐增开发商赚得笑哈哈，宽松货币政策也远远还没退出，面对房价调控措施开发商普遍直言不差钱，他们会降价销售吗？对此我表示怀疑。<br />
<br />
&nbsp; &nbsp; 解决中低收入人群的住房问题，最终还是要指望廉租房和经济适用房。政府要拿出从商品房高房价中拿到的好处来为买不起房的中低收入人群造廉租房和经济适用房。保障国民住房权是一个政府不可推卸的责任。</div>

							
							
							
							
