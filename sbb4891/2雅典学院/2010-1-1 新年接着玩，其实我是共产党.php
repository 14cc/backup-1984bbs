													<h2>新年接着玩，其实我是共产党</h2>
						
						
																											<div id="postmessage_407931" class="t_msgfont"><strong>陈sir来访</strong><br />
<br />
一年前，受右派文人蛊惑，我签了《08宪章》，签完陈警官就找到我。但我以博大壮丽的心胸和高贵文明的举止成功策反陈sir，我们成了朋友。<br />
<br />
最近，陈sir来访。<br />
<br />
你的刘晓波被判了11年啊，陈sir说，你有什么感想呢？<br />
<br />
我说，当时以为这东西会像刘晓波的其他垃圾文章一样成为过眼烟云呢，没想到竟然把他捧红了，平添其政治资本；过几年他就会被流放美国，做寓公，拿老美的钱。我上当了！<br />
<br />
陈sir点头称是。他说，你给我的George Carlin DVD和李敖全集都看完了，《V字仇杀队》也看了。唉，中国人真是软蛋，明明被我们党压迫得惨不忍睹，却乖得像绵羊一样。<br />
<br />
我说，是啊。我支持你们继续压迫他们，低等动物只配低等环境。<br />
<br />
陈sir说，你跟我们共产党想一块去了，不拉你入党真是失策。<strong>你可以告诉你的右派朋友，叫他们不要再玩火，我们共产党就是死，也会拉他们陪葬。</strong><br />
<br />
临走前，我送给陈sir《来生不做中国人》，他非常高兴的收下了。<br />
<br />
部分胆小怕死的坛员不必魂飞魄散，陈sir不知道1984bbs。我虽然从骨子里憎恨黄皮人，但黄皮人卖友求荣、虚伪狡诈的优良传统，我一点不曾沾染。冰清玉洁、文明高尚是我真实写照。<br />
<br />
<strong>慢性瘫痪</strong><br />
<br />
陈sir告诉我很多内部消息，贵国确实已日暮途穷，不可逆转。<br />
<br />
贵国很多关键系统已日渐瘫痪，比如城市体系和生态循环，会慢慢失去自我调节的能力。社会成员会逐渐兽性萌动，成为<strong>半兽人</strong>，残忍好斗，破坏家园。<br />
<br />
人多就是污染。对贵国的肮脏环境，我深有体会。现在如果在外面逛太久的话，我喉咙里面总像有异物，想要呕吐。有时找个洗手间，吐得眼泪都快掉下来。<br />
<br />
坐火车或是长途汽车，我都会事先准备一个大口径的饮料瓶，专门用来呕吐，下车后扔到垃圾桶。每扔一瓶，我对黄皮人的厌恶就增加一份。<br />
<br />
所以，我喜欢雨天，因为只有那时我才敢大口呼吸。<br />
<br />
黄皮人不懂自我反省，一个个都趾高气昂，唯我独尊；而社会也就没有修复完善的力量，积重难返。最后瘫痪。按陈sir的说法，这只需要10～15年。<br />
<br />
<strong>弱智太郎</strong><br />
<br />
早前，与日本网友聊天。他提出一些很有洞见力的问题：“为什么中国人看上去都傻傻的，一副痴呆的样子？为什么中国人都有一双<strong>死鱼眼睛</strong>？”他给中国人取了一个绰号：弱智太郎。<br />
<br />
我非常佩服日本网友的观察力。确实，得益于长期污染中毒，中国人看上去就是弱智。稍有理智的人，都会怀疑中国能文明到哪儿去，都会质疑所谓的巍巍五千年。<br />
<br />
有时，出于人道主义，我也想帮帮苦不堪言的中国人。但一看到中国人翻白呆滞的死鱼眼睛，我立刻打消助人之意。他们的眼睛从来没有激动，没有感谢，永远直勾勾的扫瞥。“许三多”<strong>王宝强</strong>之所以成功，就是中国人从他的眼睛里看到了自己的影子。<br />
<br />
这位日本网友和我都喜欢“<strong>钢铁侠</strong>”，他说如果有Tony Stark的能耐，他会灭掉金正日。我说，如果我是钢铁侠，我会把中国人打包送到火星上去，反正他们的生存能力强、繁殖像蟑螂一样。到时候，我们也绝不干涉火星内政。<br />
<br />
<strong>变质农民</strong><br />
<br />
我抖你个<strong>政治正确</strong>的斜逼，老子凭什么假装关心农民？我深知贵国农民的德性，几千年来，他们从没有长进，永远苦大仇深，愚昧无知，烂活，穷忙。<br />
<br />
这几年，眼看别人的钱越赚越多，有着优良仇富传统的农民急了，开始不择手段，想方设法折腾榨取钞票。过去仅有的田园诗意早已不存。去年，我就亲眼看见一个农民放火烧了一座青山，说是为了种豆子。<br />
<br />
我在西安的时候，也接触过一些农民。有一次，因为我不买他的水果，他竟然破口大骂20分钟。我赶紧用手机录下来，作为我学习陕西话的绝佳教程。<br />
<br />
贵国的农民早已变质，不值得过多关心。没看到他们正削尖脑袋往城里钻吗？<br />
