													<h2>王  丹：假如八九民运成功（认真读下去）</h2>
						
						
																											<div id="postmessage_669638" class="t_msgfont">谈到六四，我最常遇到的一个问题，就是：假如八九民运成功，会是怎样？尽管历史已经发生，不能假设；但是这样的问题从来没有中断过，所以我还是想谈谈自己的看法。<br />
<br />
要回答这个问题，首先就要定义什么是八九民运的“成功”。外界对八九民运最大的误解之一，就是“如果你们上台，就会比共产党更好吗”这类的质疑。这个冠冕堂皇的质疑其实完全是一个假问题，因为八九年的学生从来没有提出取代共产党，我们自己上台的主张，而且不管八九民运最后如何发展，也根本不可能出现所谓学生领袖成为国家领导人这样的事情。有些人拿这些莫须有的推测作为现实中的质疑理由，然后站在道德的制高点上评判历史，这是极大的荒谬。<br />
<br />
成功，指的是达到目的。八九民运的政治主张最早是在1989年4月18日由包括我在内的学生代表在人民大会堂会见中共中央和国务院信访局领导的时候提出的所谓“请愿七条”，包括正确评价胡耀邦同志的是非功过，彻底否定“清除精神污染”“反对资产阶级自由化运动”，为在运动中蒙受不白之冤的公民平反；公布国家领导人的年薪收入及一切形式的收入；允许民主办报刊，新闻自由，限期解除报禁；增加教育经费等等。在运动发展过程中，陆续有更多的政治主张出现，但是大致的范围也与上述“七条”有类似之处。但是我认为，如果要确认什么是八九民运的成功，还是应当以5月13日学生绝食提出的两个条件作为权衡标准，因为绝食导致学生运动转化为全民民主运动，之后全国的声援力量都集中在要求政府接受学生的绝食要求上，因此，假如八九民运成功，那么就意味着，政府最终接受了绝食学生的两个要求。<br />
<br />
这两个要求是：第一，要求政府迅速与北京高校对话代表团进行实质性的，具体的真正平等的对话；第二，要求政府为这次学生运动正名，并给予公正评价，肯定这是一场爱国民主的学生运动。<br />
<br />
因此，讨论“假如八九民运成功”这个问题，就是要讨论，如果政府开始与学生对话，并肯定了学生运动的爱国性质，对于中国未来的发展会有什么样的影响。我认为，最大的影响会是以下三个：<br />
<br />
第一，如果八九民运成功，以赵紫阳为代表的党内改革派的力量势必得到巩固。众所周知，赵紫阳是中共高层领导中最倾向于市场经济改革的，也是最具有开放意识的领导人。如果赵紫阳进一步拥有决策权力，在经济改革上，他应当会引导中国进行更加深刻的市场化改革。这个趋势，从1988年开始推动《破产法》就可以看出端倪。换句话说，如果八九民运成功，中国不仅不会陷入混乱，相反的，会使得经济改革的步伐更加坚决。<br />
<br />
第二，如果八九民运成功，早在1988年就开始启动的政治体制改革自然会在民意的强烈支持下顺利推进，这尤其包括新闻自由的部分。也就是说，经济改革的推进，就会在一个有良好的舆论监督的环境下进行。今天即使是中共，也承认只有加大舆论监督的力度，才能有效遏制弥漫全国的腐败现象；那么，如果言论自由早在1989年就开始拓展，腐败就不会象今天这样使得中国的机制病入膏肓。<br />
<br />
第三，如果八九民运成功，就开启了政府与社会对话的先例。事实上，中共十三大的政治报告，在鲍彤的主导下，已经确立了以社会协商对话 作为改革的重点的方向，而学生提出对话，正是呼应这样的政治体制改革主张。今天的中国，政府与人民同心同德的景象已经一去不复返，人民对政府的信任荡然无存，这是很多社会矛盾最后都采取激烈的方式呈现的主要原因。在改革进入到政府与社会进行利益博弈的阶段，社会稳定的根本保障就是政府与社会能够有对话的管道，双方才能齐心合力确保转型的平稳进行。台湾的经验就是最好的借鉴。因此，如果八九民运成功，可以想象的是，改革的社会环境会更加稳定。<br />
<br />
当然，假如八九民运成功，对中国的政治经济社会诸多方面的发展，会有更多更加深刻的影响，但那是需要时间来慢慢展示的。至少，以上三点是我们在短期内可以预测的趋势。简单讲就是，假如八九民运成功，中国会更快地进入市场经济发展的轨道，而那样的经济发展会是在一个政治搞个的框架下进行，而民主化的推进会相应减少今天出现的严重的社会不公的问题。这样的社会发展，也会是在政府与社会不断对话的过程中进行的，这将有助于一个公民社会的成长壮大。那样的一个中国，难道不是我们更乐于见到的吗？</div>

							
							
							
							
