<br />
&nbsp; &nbsp;&nbsp;&nbsp;这篇文章的原标题是《关于郎咸平，几年时间终于找到一条完整的线索》，2007年发在某网站，现在重新发一下，排版和个别字句调整一下，内容基本不变。<br />
―――――――――――――――――――我是传说中的分割线―――――――――――――――――――――――<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;我是在05年开始看《财经郎闲评》的，当时上海的第一财经频道每个礼拜都放。除了首播，我还要看重播。当时我在上海电视台上班，内部引用的尼尔森数据表明，这个节目是收视率是最高的。<br />
<br />
&nbsp; &nbsp; 我开始看郎咸平时候感觉很不错，比体系内的大学、党校、社科院里那帮东西讲得有水平多了，05年前，有好几次的MBO，其实国企老总做的也很漂亮了，但都没逃出这个“郎监管”的法眼。我感觉到底是文明世界接受的教育，然后又是身在自由一些的香港看得清，也敢说话，有次还在我的母校讲演，人的腔调不错，所以我对他特别有好感，可以说是郎咸平迷。<br />
<br />
&nbsp; &nbsp;《财经郎闲评》每集必看的日子，直到2006年初才结束――大家注意这个日子，2006年初，财经郎闲评终于停播了<br />
我让同在电视台的朋友和领导打听了一下，原因很简单，郎咸平讲上海社保的事情，自然不能播了，但这个是在06年初。<br />
 <br />
&nbsp; &nbsp; 同样在2006年初，我离开电视台，先到一个党报，后有辗转到一个党厂里上班，2006年8月的时候，有一个饭局，我在台面遇到了电视台领导――总编一级的领导――在电视台的时候，我都没见过面的“高档”领导――我问他，郎咸平那么好的节目还会放么？<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 他说，不可能了，上面不让。<br />
<br />
&nbsp; &nbsp; 一个月後，陈xx进去了，最公开的理由是社保问题，而这离开郎咸平说社保的事情已经大半年有余。而我直到这个时候才发现郎咸平的深不可测。仔细想想，问题有三。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;1上海是何等地方，媒体控制，媒体的谨小慎微全国首屈一指，居然他的一个节目能够播放数年，开骂全国――这样的节目，别说媒体保守的上海了，当年在全国都非常罕见，几乎是没有吧？<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 2几乎以一人之力，到处演讲，煽起舆论，让大众了解“真相”，最终“影响”决策而叫停mbo，如此事例，说“一言而为天下法”有点夸张，不过说他“匹夫而为帝王师”却丝毫不过分。又是全国罕见，甚至历史罕见。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 3财经郎咸平节目最终被取消的直接原因是社保，比陈xx东窗事发早半年有余，最终一个“学界”人物，居然能够提前那么长时间就战胜一个前途无量，政局委员级别的诸侯，这在全国又是罕见的。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;1、2、3三个疑点，三个都是全国罕见的巧合加在一起，这也太巧合了吧：）<br />
<br />
&nbsp; &nbsp; 所以我觉得郎不仅学问好，人品好――看得明白问题，也敢把问题说出来，更感觉其背景深不可测――没有相当的实力，哪里来这些巧合呢？<br />
<br />
&nbsp; &nbsp; 直到最近（注，写作文章是2007年夏天），郎在多伦多讲演，明确提出（中国）现在经济问题，单纯靠经济手段无法解决，要靠政治手段才能解决，几次提到“要开明专制”“政令要出中南海”云云，我才最终想明白了这些年，这麽多事情，总算穿到了一根线索上，从一开始就没有什么“巧合”。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 很快，人民出版社――大陆的人，想必应该知道这个出版社的粪量――居然出版了《郎咸平学术文选》，总算印证了上面所有的观点，那套书的包装很漂亮，上下卷还有个白底金字的木质盒子――估计比我的骨灰盒还要漂亮。于是，我心里也只有最后一个疑问了：那套书，为什么不按照人民出版社一贯的风格，直接叫《郎咸平选集》？<br />
