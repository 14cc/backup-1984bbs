回归使者<br />
据广东省委内部人士透露，2个月前广东省委办公厅曾收到过一封紧急政治举报信。信中谈及了中共重庆市委书记薄熙来企图利用工农、制造混乱的一个大政治阴谋。这封自下而上、通过常规信访渠道邮寄到省委办公厅的举报信，在传到汪洋手中之前曾在广东省委内传阅。可是在汪洋阅读过后，便再也没有相关的后续信息传出，省委内部人对此也是忌讳莫深。<br />
据信件内容看来，此人（写信者）很有可能是薄熙来的幕僚或者亲信。信中所谈及的“阴谋”，的确阴险隐蔽至极，读后让人感到后怕。<br />
信中有两点内容很诡异：第一是牵涉到了香港廉署的人。联想到近日中央在政改问题上对港府做出的巨大让步，不能不让我们怀疑：党内权斗是否已经波及香港政府？第二是信件末尾说：“截止自5月4日，我尚不确定军队内部是否有人知晓此阴谋”――薄熙来此前曾放出“堡垒是从内部攻破的”言论，而且薄熙来同邓小平之子邓朴方都已“冰释前嫌”，却惟独仍与习近平不相往来。这是否是因为作为“王储”的习近平，必然地阻碍到了薄熙来“军变夺权”的狼子野心？<br />
如果薄熙来真的用此阴狠招数发动信访群众协助他夺权――那么这封倒戈的举报信，无疑足以让胡温誓死铲除薄熙来。可怜薄熙来机关算尽，却反误了卿卿性命……<br />
<br />
附：《致广东省委书记汪洋的一封信》<br />
我虽尽力，但仍不敢保证此信能否安全送达广东省委……谨慎起见，容我先作如下声明：<br />
本信件直接关乎到汪洋书记的利益，且不仅仅只关乎到汪洋书记的利益；<br />
本信件是夺权阴谋的告密信。党内政斗向来杀人于无形――如果有人认为我党真像媒体上报道地那般一派和谐，政斗之事纯属虚构的话，请务必向上级或者知情人士咨询，以避免因为自己的单纯而耽搁此信；<br />
本信件所涉及的内幕信息截止自5月初，距今已过去近2个月，因此本信只能反应5月份之前的一些情况，无法证明现在的情况是更好、还是更糟糕。<br />
我虽尽力，但仍无法保证此信能否顺利送达广东省委――甚至无法保证此信能否顺利进入广东省内――因此，请恕我不留下直接的联系方式与私人信息。<br />
如果汪洋书记有幸收到此信，且欲了解更多信息，可以联系香港廉政公署廉政建设研究中心主任岑家辉。同岑家辉先生联系时，请务必出示能证明是汪洋书记本人、或者是汪洋书记委派的代表的确切证据。如果岑家辉先生确认联系者的确是汪洋书记、或者的确是委派自汪洋书记，他会告知您们我的联系信息。<br />
如果汪洋书记真的有意联系我以了解更多的信息――那么，请汪洋书记务必做到这一点：在党和国家的大利益面前，请您摒弃身份地位与门户派系的偏见――如果无法做到这点――那么，请不要同廉署联系。因为：只有摒弃偏见坦诚交流，我才可能告知您更多更重要的信息。<br />
我对本信件所涉及内容的真实性负责――您们也应该清楚，虚构信息、诽谤中央委员对我而言，百弊而无一利，我没必要拿自己的性命做赌注去开玩笑。<br />
现在，先开始谈这个夺权阴谋的前奏背景<br />
两件事：<br />
第一：以薄熙成为代表的一些左翼以及“太子党”的商业力量，在过去这半年的时间里，已经在中国诸多省份投资创建了数百家新公司或者分公司，间接控股。同温云松创办的新天域资本一样，他们虽然不是这些公司的直接老板，但却是这些公司的实际领导人。<br />
第二：重庆市委书记薄熙来派人在全国范围内，收集上访群众的资料。如果我没推测错的话，广东省委人民来访接待室的领导也可能在过去的半年内，不经意地泄漏了许多上访群众的资料。<br />
这两件事情，看起来也没什么。商人投资公司是很正常的事情。上访群众的私人信息也不是什么秘密，别说贵为中央委员的薄熙来――即便是一个普通的市长，他要收集的话，也可以弄到很多很多。<br />
然而政治阴谋的特点，就是你根本不能从表面上看出一件事情背后的目的……<br />
――我们都知道，中国的上访制度本来就是一个陷阱：登记了上访群众的身份等私人信息，却从来没有办实事。有些上访者的个人信息甚至可以全国共享，这点我就不多说了。薄熙来收集如此大量的访民信息，目的何在？<br />
薄熙来此举的目的，就是打击所有可能威胁到他竞选十八大常委的竞争者。<br />
此举的阴狠之处在于：上访群众都是一群愤怒的、淳朴的人民――这群人有2个共同的特点：第一是生活条件不怎么好并且多数人知识水平不高，第二是憎恨当地政府官员。这点，我也不需要多说了。<br />
左翼力量和一些弃政从商的先辈子弟投资的新公司也有2个特点：第一是要有很多的钱需要花出去（因为投资本来就是“花钱”，至于能不能收回这些钱，就看企业领导层的能力了）；第二是需要招聘很多的新员工。<br />
现在，请您们细心想象一下这个情景：<br />
一群被广东省委赶回家的上访群众，哭哭啼啼、满腔怒火地回到老家――这时候，有人向极度失落的他们伸出了援手，将他们免费培训成为他们公司的员工，并解决了他们的生活等难题。这些神秘的好心人甚至可以通过政治渠道，帮这些访民解决一些领导不想管的、芝麻大的小问题。<br />
这些本来处境困难的上访群众，不仅顺利解决了一些芝麻大的小问题，还摇身一变，变成了拥有固定工作收入的蓝领――他们对这些神秘的好心商人的感激，可想而知。<br />
以薄熙来为首的商业力量的目的，就是用一种极其隐蔽的手段“积累人民”。<br />
我再强调一次这个词：“积累人民”。人民什么时候作用最大？――不是创造生产的时候作用最大，而是搞破坏的时候作用最大。100万温顺的人民，最多只能撑起一家勉强可以算得上是著名的企业――但100万造反的人民，在这个信息发达的社会里，却足以吸引数十亿地球人的注意力。<br />
中国现在的社会状态是：即使远在新疆的十几个农民起来围攻当地政府，中南海也会坐立不安。――在阴险的政治家眼中，人民最大的作用就是“添乱”。<br />
――这，就是薄熙来的目的。<br />
我不知道以薄熙来为首的商业力量，在这半年内到底“积累”了多少的人民――但我清楚地知道他们的最终目的：<br />
2012年十八大之际，汪洋书记动身前往北京……汪洋书记离开以后，广东会发生什么事情呢？――那时候，这些曾经是访民的“企业员工”，就会来到广东省委大院面前，随便找个的借口“示威”或者“请愿”了。――不仅仅是广东――安徽省委大院、云南省委大院……恐怕都会遭此厄运。<br />
