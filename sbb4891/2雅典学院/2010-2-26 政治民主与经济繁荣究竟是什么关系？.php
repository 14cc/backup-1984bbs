													<h2>政治民主与经济繁荣究竟是什么关系？</h2>
						
						
																											<div id="postmessage_493528" class="t_msgfont">去35年，世界各地民主国家的数量不断增加。一切皆始于上世纪70年代的西欧，希腊、西班牙和葡萄牙的专制政权相继倒台。80年代，运动蔓延至东亚和拉丁美洲，韩国、台湾、菲律宾、巴西和阿根廷迎来了民主。90年代，种族隔离制度在南非的终结，伴随着选举民主在非洲大陆大部分地区的传播。最具戏剧性的突破来自1989年柏林墙的倒塌和前苏联帝国的解体。<br />
<br />
自冷战结束以来，西方的政治讨论往往认为，上述一系列事件不仅仅是一种让人愉快的巧合。在许多分析人士看来，它不仅证明了人类对自由的普遍向往，也证明了自由民主制作为一种政治制度的内在优越性。如罗纳德&#8226;里根(Ronald Reagan)所言，“自由发挥了作用”。实行民主的国家将在经济领域取得成功，而专制政权注定会失败。<br />
<br />
上世纪90年代“信息革命”的到来，为上述乐观理论增添了另一层意义。人们普遍认为――从鲁珀特&#8226;默多克(Rupert Murdoch)到比尔&#8226;克林顿(Bill Clinton)等各色人物都是如此――在新“知识经济”时代，只有那些允许思想自由传播的民主国家才能最终实现繁荣富强。这一信条使美国更容易对中国的崛起报以随和的态度――根据该理论，中国不是变成一个民主国家，就是在经济上一败涂地。<br />
<br />
但如今，中国已是世界第二大经济体，而它几乎没有表现出转变为民主国家的迹象。现在我们很容易想象，当中国的经济规模超过美国时，它仍将是一个专制国家。根据目前的趋势，那一刻将在本世纪20年代到来。<br />
<br />
世界头号经济强国可能很快会是一个非民主国家的概念，让后冷战时代许多令人宽心的假设都站不住脚了。阿泽尔&#8226;盖特(Azar Gat)有关民主与政治和经济成就之间关系的新书也因此显得特别恰逢其时。<br />
<br />
盖特是以色列特拉维夫大学(Tel Aviv University)的教授，他的上一部著作《文明世界的战争》颇受好评。新书是一部主题散文集，收录的文章大多已在其它地方发表，因此读起来不像专著那么有趣，但盖特的中心主题十分发人深省。<br />
<br />
他的基本观点是，西方理论家假定自由民主制度效率出众，是太过自满了。他很有说服力地指出，前苏联体系之所以难以维系，不是因为它的专制，而是因为它是共产主义制度。信奉资本主义的专制国家――比如上世纪40年代以前的德国和日本――有一段时期曾经成就斐然。盖特表示，日本人和德国人证明，专制国家可以把经济效率、公众支持和军事实力结合起来，使国家的运转变得十分高效。<br />
<br />
盖特认为，决定第二次世界大战结果的，并不是由自由民主制度的出色效率，而是由参战国的相对规模。简言之，日本和德国太小了。相比之下，战争胜利的原因在于美国的国土面积，而非它的民主政体。<br />
<br />
盖特写道：“要是没有美国，自由民主国家很有可能输掉20世纪的宏伟战争……后人对自由民主制的评价也就很有可能呼应公元前4世纪希腊人在斯巴达战胜雅典后对民主的否决。”他或许还应该指出，正是由于非民主的前苏联做出了巨大牺牲，才确保了“民主”在1945年的胜利。<br />
<br />
下一代人面临的重大世界问题可能是，中国能够在多大程度上复制20世纪上半叶那些专制政权的成功。盖特没有对未来妄下定论。但他对过去100年历史的研究是一记有益的提醒：民主的胜利可能比沉浸在冷战结束喜悦中的许多人所设想的更加偶然。<br />
<br />
