第五，综合上述分析。对于民主国家和专制国家我们应该有不同的策略。民主国家不能简单盲目乐观于民主本身的强大的物质实力，紧急情况下放弃一定的个体自由保家卫国是非常必要的。到底情况紧急到什么程度，要放弃多数个体权利，这个有赖于民主国家全民的政治成熟。对于专制国家的民众而言，在统治者允许的范围内发展经济，在被束缚领域争取权利是最好的选择。如果能够实现这种良性演进，对本国的民众和统治者，以及对周边的国家，都是天赐之福。可惜的是专制国家的这种演进往往更有赖于统治者的智慧和变化。</div>

							
							
							
							
