错误，带来的就是资源的浪费和生产的效率低下。但是值得注意的是，这一过程往往是个长期的过程，短时间内，计划经济的确会使某一产业得到迅猛发展，但是长期来看，依照目前的历史和经验，还没有一个计划经济国家没有在经济上失败的。<br />
<br />
中国在改革前的经济状况大家都很清楚，商品匮乏。目前经济状况好转了，很多人得出了一个奇怪的结论：专制国家也能搞好经济。<br />
<br />
但是他们没有看到的是，我们现在的专制，相比改革前的专制，已经进步了很大一步。而我们的经济，也正是在政府让步出的这一步专制权力中发展起来的。我们能看到的是，中国人对财富的渴望远远大于世界上任何一个民族。仅仅是这一点专制的让步，这一点自由的市场，就让我们创造出了如此之多的社会价值。<br />
<br />
但毕竟国内仍然是一个非完全自由市场，内需有限，创造有限，在取消就业分配之后，最突出的问题就是劳动力过剩。这一点上，中国政府的外交策略很成功，成功的把本国过剩的劳动力转化成商品输出国际市场，但是代价也不小，就是国内的通货膨胀。<br />
<br />
目前，专制回头现象有些明显，前一个阶段开放后所带来的后遗症还没有痊愈（流动过剩）。如今的缩小国内自由市场，势必更加加剧流动过剩及社会矛盾。<br />
<br />
最雪上加霜的是，国际社会也对中国这种经济模式不认同，认为中国只解决了自己的就业问题而没对解决他们的就业做出帮助。一片欣欣向好时大家谁也不提，经济危机中解决就业是最关键的，对中国的贸易壁垒势在必行。<br />
<br />
解决的出路有么，当然有，继续深化改革，减小专制权力，扩大自由市场，但是我非常悲观的看到，本届政府不会在这一点上做出任何让步。<br />
<br />
下届政府呢，很难说，但如果专制持续目前这种倒逼态势，经济情况会愈加糟糕，恶性通胀也许不远了。</div>

							
							
							
							
