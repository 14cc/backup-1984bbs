<br />
　　蠢货梁文道<br />
<br />
　　一<br />
<br />
　　今天上MSN，一位美女给我留了言：<br />
<br />
　　2009年2月26日， 梁文道在南方周末上写道“尤其是英法联军劫掠圆明园的那一回，因为列强刚刚才在 1899年加入了禁止战时掠夺的“海牙公约”。”把常识性的火烧圆明园事件穿越到了八国联军时期， 其学术水平再一次受到网民的强烈质疑。<br />
<br />
　　这位美女我心仪了许久，曾经一度打得火热，差点就擦出火花。无奈她一直怀疑我的道德操守，没给任何机会让我饥渴的灵魂附她完美的肉体。我费了九牛二虎之力，假装情伤未愈，扮演忠贞不二，甚至以婚姻为诱饵，也就和她在星巴克喝了两钟头咖啡，手都没摸一下。不过，我们弄清楚了彼此的喜好，交集点其实蛮多，例如不爱国不爱社会主义，不爱民不爱民主运动。我们讨厌的人也很一致，除了余秋雨、成龙、杨澜、徐静蕾这些公认的烂人外，还有道貌岸然的左棍梁文道。说到梁文道，大家马上就有相见恨晚的意思。啊，你也讨厌他呀，不少人挺崇拜他呢，她惊讶地说。我说，切，那都是没见过世面的大学生吧。她说，也不是啊，我身边不少公司白领也喜欢。我又切了一声，说，大陆人嘛，受的是装逼教育，余秋雨骗得，于丹骗得，为什么梁文道骗不得。她说，就湿就湿，这些人差不多，装的雀湿凶。我笑了：演技好才是真的好，CCAV上有温影帝，凤凰AV上有梁影帝。<br />
<br />
　　美女发来这则留言，可能是想勾起我美好的回忆吧。那个懒洋洋的下午，我们四目交会、相濡以沫，一起毫不留情地痛贬梁文道，从中获得了智力上的优越感。骂余秋雨就没这种快感了，他早就暴露了自己的身份，一坨年代久远的5毛。梁文道不同，他还热气腾腾呢，冒着袅袅的轻烟。在许多人还嗅出芬芳的时候，我们躲在一侧，掩鼻而笑，这是人生中多么难得的一件快事。<br />
<br />
　　啊，我的小美女，你真是我的心肝宝贝，我的蜜糖打铃，我的鸽子玫瑰，连梁文道苦心经营的假面都能识破，我真真爱死你了。我原谅你不和我上床，我原谅你让我请你喝咖啡却不和我上床，我原谅你让我请你喝咖啡听了某只暴涨股票的内幕消息却仍然不和我上床。<br />
<br />
　　二<br />
<br />
　　其实我和梁文道有一位共同的朋友，知道他一些事情。不过出于高风亮节的理由，不以流言作为根据来评判人，我决定舍弃道听途说的那一部分。但我可以说一下这位朋友对梁文道的看法，他是从爱护和偏袒的角度去看的，他认为梁是一位典型的书呆子。我表示不同意，梁就是蠢货，这和读书多少没有关系。<br />
<br />
　　我很早以前听过梁文道的一次讲座，从此就奠定了他留给我的坏印象。那次讲座他沾沾自喜说了两件事，都让我体验到了什么叫五雷轰顶。一个就是他标榜自己是关心劳工的左派。他怎么关心的呢？他想了一个先锋艺术的方案，游说某些大企业树立纪念碑，专门纪念那些因工殉职的工人们。他认为，这个方案对于资本家是很吸引的，因为可以通过这个纪念碑洗刷手上的血迹，树立良好的公众形象。而他自己，则利用资本家的负罪心理为劳工做了一件有意义的事。我实在没弄明白纪念碑能为死难的劳工带来什么好处，但记得本雅明说过，法西斯美学的重大特征就是利用死者，并且迷信纪念碑的特殊价值。后来，我听的就更明白了，纪念碑的钱是资本家出，但它的设计和建筑是交给梁文道的艺术家朋友的。听起来，梁文道更像是包工头。这和劳工利益有何关系呢？<br />
<br />
　　当时第二件梁文道引以为荣的事情是，他有时会被某些地方政府请去当艺术顾问。毕竟在凤凰上脸混熟了，成了着名知识分子，广东某些富裕的乡镇会请他参观，请他提供文艺建设方面的建议。当然，这是付费的。这种事情，余秋雨就干过不少。不过那时候，余名气大，往大城市走，梁名气小，往小乡镇走。梁讲起自己的考察经历是很兴奋的，也是用他一贯的所谓社会批判的方式来讲，但批评的落点却很奇诡。他纠缠于这些政府的文化工程品味太低，土气，不够现代，不够先锋。其实，对中国环境有一丁点了解的人都知道，艺术风格都是其次，这些属于政绩工程，无论怎么搞，最终都是为了腐败，钱入了自己口袋。当时讲座上就有听众说了，古希腊也好、洛可可也好，都一样，工程才是目的。但梁文道好像没听到，继续沉浸在艺术史的讲述之中。他好像认为，这些工程的症结在于没有“文化底蕴”，如果早点请他顾问，听他的劝告，换成后现代主义的艺术品，或者死难工人的纪念碑，就完全没有问题了。<br />
<br />
　　整场讲座听完，我基本明白梁文道的好恶。他的确是批判型的，但他的锋刃指向的是商业和市场，对于眼皮底下的这个政府，却不温不火的，所谓批评也是谏臣式的，人民内部矛盾的。很明显，他故作清高，对金钱有厌恶感，却不排斥依附于权力渔利。<br />
<br />
　　三<br />
<br />
　　对梁文道有了基本的认识，自然就不会对他有太大的兴趣。可是没办法啊，有时电视上会听到他大放厥词，报刊杂志上时有他的干瘪文字，逃得了初一，逃不了十五。但就是这么零零碎碎接触到他的“真知灼见”，从未让我失望的是，每次都能让我找到厌恶他的理由。有一次印象很深，凤凰搞什么选美，他负责向一位新疆的参赛者发问。他问了一个奇怪的问题，问对方有没有因为自己是新疆人，在公共汽车等公共场所受人歧视。那位参赛者一下子就语塞了。我也愣住了，这么一位大美女在公共汽车上有可能被性骚扰，怎么可能受歧视呢。难道，梁文道认为新疆人在中国，相当于黑人在美国？在中国，新疆人受汉族人歧视？这真是重大发现，批评知识分子就是不一样，有异于常人的魔鬼天眼。<br />
<br />
　　类似这种睁眼说白话的例子，在梁文道身上是很多的。例如他认为中国的经济政策是奉行新自由主义，所以问题多多。实际上只要往中国的学院里走上一圈，和经济系学生聊聊天，就知道中国占统治地位的主流经济理论，除了马克思主义的，就是凯恩斯主义的。改革开放以来，保守派的理论资源是马克思主义的，改革派的理论资源是凯恩斯主义的。中国从来没有新自由主义兴盛过，在民间的舆论都占不了上风，更别说影响官僚制定政策了。但梁一直把新自由主义作为靶子，这样就方便他推销自己的新左观点。梁的观点是什么？很简单，陈腔滥调了，乌有之乡和强国论坛大量批发。让政府在社会中担当大法官，干预社会，弘扬正义，锄强扶弱，消除不平等。<br />
<br />
　　这种观点比老共温和一点，因为新左高举的旗帜是民主，鼓吹人民做主。不过，切不可以为就真的不同于老共，实践的结果就是悲惨的“拉美化”。看看社会主义新贵查韦斯肉麻吹捧中国就知道了，老共新左就是子承父业，孽种流存。梁文道好不了多少，他常常夸耀自己还相信“全世界无产者联合起来”，他要用真马列主义对抗当局的假马列主义。从善意的角度看，这说明梁对改革开放的起源是很无知，他不知道当局放弃真马列，是因为真马列在毛时代造成了无数的悲剧。马列坚持的结果，有苏联的饥荒和大清洗，也有大跃进的三千多万孤魂野鬼，以及文革腥风血雨的无产阶级专政。梁文道怎么好像不知道呢？他经常为马克思主义辩护，不过手法十分奇特，最近就有这样一条：<br />
<br />
　　“当我第一次和大陆的朋友聊起马克思，我就觉得有点不对劲。他们说的那个马克思真是我们所知道的马克思吗？德国人马克思怎么会说出“人定胜天”这一类的话呢？莫非还有一位姓马名克思的中国人，只是我不知道？后来我才晓得，原来接受“马列主义教育”不一定就得读过马克思的原典；即使是读，也可以择章摘句，片断零碎地读。”<br />
<br />
　　显然，梁文道认为，老共错在没真正吃透马克思思想。马克思主义棒的很，只是没掌握好而已。这种辩护方法和某些新纳粹主义者为希特勒辩护是一样的。如今很多新纳粹分子都认为希特勒是伟大的思想家，他不应该为屠杀犹太人负责，实际上是某些纳粹军官没有吃透希特勒的思想，只是片面地理解，采取了错误的方法，才导致了失败。否则的话，世界早就是国家社会主义的天堂。这就是传说中的砌词狡辩啦，而且是最无耻的一种。在这种辩护底下，任何邪恶的思想都无罪，都可以因人而异。那还有什么必要做知识和观念上的争论，让贤明和圣人来统治我们就好了。我们不需要好的观念、好的制度，我们需要好的统治者。<br />
<br />
　　除此之外，梁文道还有一个惊世说法，令人难忘。他鼓吹“普世价值”，却天才地认为，“全世界无产者联合起来”就体现了这种观念。未免太搞笑了吧！这句话横看竖看，都证明了马克思反对“普世价值”。马克思主义打破国界，但强调的是阶级间你死我活的斗争。适用于无产者的，并不适用于有产者，这怎么是普世价值呢？纳粹分子也是跨越国界的，他们的口号是“全世界优良的种族联合起来”，如果马克思主义是普世价值，纳粹主义也同样是。<br />
<br />
　　亏得梁文道还自称读书人，连这么基本的常识都没有，还好意思贩卖常识。这样的人不是书读多了，而是屎片读多了――正一死蠢。<br />
<br />
