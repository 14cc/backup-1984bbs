													<h2>儒家传统是中共专制生命力特别强的重要原因</h2>
						
						
																											<div id="postmessage_371406" class="t_msgfont">“六四”大屠杀今年满二十周年了，二十年来，在中国大陆社会，六四的记忆已经消褪得几乎没有血色――新成长起来的“八零后”、“九零后”们，普遍不知“六四”为何物，而在那些知道“六四”、甚至亲身参与过八九民运的人眼中，“六四”越来越成为一组少不更事的模糊镜头，二十年来，回首“六四”，海内外华人们越来越频繁地轻摇着经济动物的脑袋、发出算计的叹息声；同时，各种漠视人血的高论竞相泛起――什么学生闹事是在破坏“改革开放”的大好局面、什么杀人维护社会的安定、什么没有“六四”屠杀就没有后来中国的经济繁荣等等。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;　仿佛当年和平示威的学生死得好，为了所谓的经济成就，“安定团结”局面、“中国崛起”，就该不定期地杀一批批活人献祭……<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;　恐怕也只有中国人的价值观，才能够作如此“灵活”的变通，对西方人来说，罪恶永远是罪恶。因此犹太人会那样“记仇”，他们至今铭记近两千年的“哭墙，不会因为罗马帝国盖世荣耀而忘记罪恶，许多中国人呢，才吃到两块豆腐，就为二十年前的杀人犯叫起好来。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;　在共产主义信仰早已破灭的今天，这些歪曲的观念，不仅在大陆很有市场，也在海外华人中大有市场，生活在儒家社会的新加坡人，就特别认同这种看法。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;　为什么中国人容易接受这种观念？这其实是儒家传统的价值取向在作祟。因为共产主义意识形态早已破灭，近二十年来，中共全靠民族主义和借助槽粕传统作精神支撑，而儒家越来越成中共为一己专制正名的主要工具。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;　已经普遍不信共产主义的中国人，之所以在六四问题上认同中共歪理者日众，不能不说是受儒家影响的社会和文化传统所造成的。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;　儒家价值观中深刻影响中国政治主要有三个：<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;　一是重集体、轻个体的价值取向；一是长幼尊卑观念；三是缺乏人道。儒家一贯强调“国家“、“社稷”为重、并且把君主当作“国家“、“社稷”的化身，将“国家”、 “社稷”、“君主”的利益摆在个人之上，而毫无个人权益的概念，儒家（主要是孟子派）虽然也强调“民为重”、“以苍生为念”，但不论是“民”还是“苍生”，并不是具体的个人，而是整体的概念，类似于中共滥用的“人民”概念。因此，儒家是一种集体主义的论理思想，它把抽象的人置于具体的人之上，主张为集体而牺牲个人，牺牲“小我”，成全“大我”，这是一种压制个人的伦理思想，这种论理的价值观，是不可能生长出宪政民主和人权思想的。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;　儒家思想的集体主义属性，与马克思主义内在是相通的，受儒家社会传统的潜移默化深刻影响，中国人普遍形成了一种思维习惯，这种思维习惯使其远比欧美民族更容易认同统治者侵犯个人权益的行为，只要统治者高举“人民利益”、“国家利益”、“民族利益”等镀金幌子，哪怕坏事做绝，中国人都容易视以为常。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;　中国儒家社会传统的重集体、轻个体的价值取向，与共产党蔑视个人权利的价值观可谓是“心有灵犀一点通”：六十年来，镇反、土改、反右…“计生”、“严打”、六四屠杀、迫害法轮功、强迫拆迁、强迫征地…中共所行的、不胜枚举的每一项暴政，无不打着人民利益、国家利益、社会利益等为全局谋、为长远计的冠冕堂皇幌子；每一项暴政在实行的当时，都没有引发整体性的社会反弹，原因就在于中国传统社会价值观对共产暴政起了某种内在的支持作用。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;　因为儒家社会传统的影响，海内外许多中国人就容易受到表面经济繁荣的迷惑深信中共七歪八邪的说辞：没有六四杀人，就没有“安定团结的大好局面”，就没有后来的“经济奇迹”云云。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;　正是儒家重集体、轻个体的价值取向，为中共顽固派“杀二十万，换二十年稳定”的做法提供了深层社会文化的支持：随着共产主义意识形态的崩溃，现在实际上在支撑中共统治的歪念就是中国儒家传统社会的“大局”观念。当年邓小平、陈云等人，对和平示威的同胞下达开枪令，没有军队将领中引发任何公开的反抗，而齐奥塞斯库对本国民众的开枪令，却引发了国防部长的抗命和高级将领的倒戈，这就是不同文化传统社会所造就的必然不同结果。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp; 因为这种轻视视个人的集体主义价值观，儒家倾向于为了“大局”而压制个性，儒家理学所高举的教条“饿死事小，失节事大”，则经典地反映了儒家这种轻视视个人的价值观。为了所谓的“整体”利益对个人采取残酷的惩罚措施，在儒家看来是完全正当的。因此儒家会主张“乱世用重典”，历史上儒家一直支持中国王朝统治者 “杀一儆百”的做法。因为这种价值观的影响，儒家思想缺乏、且难以产生人道主义精神。一千年来，随着儒家思想逐渐占据中国社会主流意识形态，中国统治者的作风和社会习气不是更文明了、而是更残忍了；自南宋以后，凌迟这种全世界最惨无人道的死刑、缠足这种有违人道的陋习，均随着儒家理学的兴起愈演愈烈、到清朝时登峰造极。儒家即使没有直接促使中国社会的残忍化，也在很大程度上纵容了中国向残忍虚伪的方向发展。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;　不能不说，儒家的这种残酷的倾向和残酷的原因，与马克思主义的残酷的倾向及其原因，内在是相通的。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;　而且，儒家与马克思主义还有另一大暗合之处，那就是无神论倾向。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;　马克思主义公开彻底否定宗教，马克思认为宗教是剥削阶级借以麻痹劳动者反抗精神的精神鸦片。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;　儒家虽然没有公开否定有神信仰，但孔子从一开始就“敬鬼神而远之”、“子不语怪力乱神”，儒家从一开始就没有彼岸世界的概念，因此儒家没有“天堂”、“地狱”等概念；孔子说：“未知生、安知死？”在孔子亲自铺设的精神框架下，儒家发展成一种纯粹现世的信仰体系；儒家大力攻击佛教“无君无父”，从来不承认转世轮回和因果报应。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;　由于因此儒家没有“天堂”、“地狱”等概念，也不承认转世轮回和因果报应，儒家的信仰者也就没有罪恶感，因此儒家的二号宗师孟子会提出“性善”说，“人之初，性本善”也成为儒家的重要信条。但实际上，人的本性自私，因为自私，人更容易作恶，因此，基督教性本恶的“原罪”说，要远比儒家的“性善”说更接近真理。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;　“性善”说的最重要影响，就是使人没有罪恶感，既然人本来就是“善”的，何来“原罪”？不信“原罪”，何来罪恶感？因此，儒家信徒缺乏忏悔精神，对儒家信徒来说，任何罪恶都可以用“人非圣贤，孰能无过”释然视之、行若无事。因为没有罪恶感，儒家信徒一方面容易纵容自身的恶，另一方面纵容君主的恶――为尊者讳：一些成功的君主，尽管穷凶极恶，却被儒家吹捧为“圣主”。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;　缺乏忏悔精神，就必定缺乏人道情感。因为失没有超世俗的崇高信念，人就容易受利欲驱动，在纯粹利欲的驱动下，人恻隐之心麻木不仁，就会更容易做出许多堕落和残暴的事情来。儒家信徒正因为没有超世俗的崇高信仰体系，因此普遍容易为利欲驱动，尽管儒家有着繁琐的道德信条。自南宋以降，大儒们几乎“满口仁义道德、满脑子男盗女娼”，理学创始人朱熹本身就是典型，明朝思想家李贽指出，道貌岸然的大儒们，无不“心存高官，志在巨富”，这生动地揭示了儒士们的虚伪和卑琐。在儒家理学登峰造极的明朝和满清，儒家（特别是儒家理学）信徒几乎成了假仁假义的代名词。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;　这就是为什么自南宋以后，随着儒家的坐大，中国的王朝越来越残暴和腐败的原因。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;　正因为容易受利欲驱动，儒家信徒在历史上远没有基督徒那种殉道的勇气，儒士们的勇敢甚至不如革命时期的共产党员。因为在共产意识形态亢旺之际，马克思的徒子徒孙们好歹还有共产乌托邦天堂的信仰，儒士们则没有任何超世俗的信仰。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;　因为无神论的影响，共产党的信徒们“天不怕，地不怕”，什么坏事都敢做；而儒家的信徒们则利欲熏心、假仁假义、“以理杀人”、以道德的名义，做出诸多腐败和残暴的事情来。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;　除列宁等个别疯狂的无神论者外，前苏联和东欧的共产政权领导人外壳虽然是共产党党徒，内在却有着潜移默化的、抹不去的基督教（东正教）影响，这种宗教的影响与其邪恶的共产信条发生着无声的、深刻的冲突，不同程度地悄然抵消着欧洲共产独裁者的邪恶，这就是作为欧洲最凶残的独裁者，斯大林会远比毛泽东更尊重宗教、文化、艺术的根本原因。斯大林于二战期间“大发慈悲”，突然放开宗教信仰禁锢；毛泽东则决不会发这种“慈悲”。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;　而中国的共产独裁者，其内在有着潜移默化的、抹不去的儒家传统影响，这种影响与其邪恶的共产信条、专制独裁思想观念暗通款曲、相辅相成、相得益彰，它不同程度地悄然强化着中国共产独裁者身上的邪恶，为其残暴决策提供强有力的人文支持。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;　这就是同为顽固派共产独裁者，邓小平、陈云、<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;胡锦涛等人可以毫无心理障碍地以屠杀同胞保专制，而前东欧国家的领导人却普遍下不了狠手的根本原因。而面对邓小平、陈云这样穷凶极恶的决策，远比当年叶利钦更为有利的赵紫阳，却毫无反抗的道德勇气。这也是前东欧国家能够一齐于1989年和平演变，而中国却不能的最重要原因。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;　共产专制在中国特别顽固的另一项原因，就是中国儒家传统土壤所长出的老人政治毒株，这是除同为华人社会的新加坡外，其他所有国家所没有的东西。在儒家的长期影响下，中国人孝敬长辈的传统，被扭曲成老人政治传统，曾经当权的老人，从王朝的“太上皇”到红朝的“中顾委主任”、“元老”，可以“交位不交权”、 “退而不休”…名不正言不顺地继续干预政权运作、甚至幕后操控政局。这种荒唐的传统，大概是其他任何民族的政治思维所无法理解的。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;　这种百年前慈禧太后垂帘听政的做法，自邓小平开始，居然再次成为中国的政治惯例。随着江泽民集团的退休、胡锦涛集团的行将退休，中国的老人政治集团越来越庞大，庞大的顽固派老人集团，无比顽固地阻碍着任何改良措施的出台，使得任何放松专制的改革成为不可能。这就加深了专制独裁体制在中国的顽固性。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;　<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;　综上所诉，为什么共产专制的生命力在中国（包括深受中国文化影响的朝鲜、越南）远比其在欧洲强大？因为欧洲有基督教消减共产主义的邪恶，中国等东亚国家却有儒家助长共产主义的邪恶。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;　随着共产主义意识形态破产，欧洲的共产政权纷纷倒塌，但在中国，中共却可以依赖儒家延长专制的寿命。这也是胡锦涛上台以来大力推崇儒家的根本原因。</div>

							
							
							
							
