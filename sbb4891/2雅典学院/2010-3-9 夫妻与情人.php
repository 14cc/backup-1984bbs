													<h2>夫妻与情人</h2>
						
						
																											<div id="postmessage_508929" class="t_msgfont">夫妻与情人是两个对立的体系。<br />
<br />
 <br />
<br />
 <br />
<br />
夫妻是以获取利益为前提的社会体系，而情人是以消耗为主体的社会体系。情人具有一切纯女性的特征。优雅娇俏的体形，温柔动听的声音，病恹恹的花容，这皆是消耗型人格的指数与特征。《茶花女》里的女主人公玛格丽特与《红楼梦》里的林黛玉就是这等消耗型人格的最佳范例：1二者皆有肺痨（疾病对身体细胞的消耗）。2二者皆用一种植物来指代她们弱不禁风的形体与灵魂：玛格丽特――茶花，林黛玉――绛珠仙草（植物性女子需要护养的消耗）。3二者皆死于痴情（剩余时间的消耗）。痴情是一项伟大而耗时的事业，这项事业要求痴情主体拥有大量的剩余时间作为资本。而剩余时间是有闲阶级的特权。就资本的本质而言，资本家按时酬工，时间代表的就是金钱。宝玉说黛玉“从来不说那样的混账话”（逼他读书上进求取功名），孰不知，这正是宝钗肯说这等混账话，赢得了婚姻的根本原因。宝钗懂得婚姻之经济本质。黛玉显然不明白这一点，她是只能是一个好的情人，她将生命、时间、眼泪，她所能拥有的一切，皆消耗于爱情之中。<br />
<br />
 <br />
<br />
 <br />
<br />
徐志摩与陆小曼经历过一段浪漫的爱情之后成婚。婚后的王子为了生存疲于奔命，往来京沪，高蹈于空，最终火焰般灿烂一把，化为灰烬。彼时，大众因徐志摩之死，对陆小曼的奢侈生活多报以指责。其实，这怎么能怪陆小曼呢？陆小曼是标准的消耗型人格，她需要的是个养护她、滋润她、能使她从感情到物质两方面皆满足，并供给她的大成本财物消耗的富有男人，而不是一位诗人。徐志摩与陆小曼的错误，错就错在将情人关系转换为夫妻关系。消耗型人格的情人却误兼了经济型妻子的职责，这必将导致家庭关系的紊乱，亦会直接导致家庭经济利益的崩溃。<br />
<br />
 <br />
<br />
然而，情爱学中有一种消耗比时间、财物的消耗更令人伤情，那便是容颜的消耗。杜拉斯的《情人》开头有一段颇为著名的文字“我始终认识您。大家都说您年轻的时候很漂亮，而我是想告诉您，依我看来，您现在比年轻的时候更漂亮，您从前那张少女的面孔远不如今天这副被毁坏的容颜更使我喜欢。”很多人将这段话熟记于心，却不知这段话是由叶芝的诗歌《当你老了》中的钻石级诗句 “多少人爱你青春欢畅的时辰/爱慕你的美丽，假意或者真心/只有一个人爱你那朝圣者的灵魂/爱你衰老了的脸上痛苦的皱纹”演化而来。杜拉斯的聪明，在于她将叶芝的诗歌话语彻底转化为她的小说话语而已。但，我想指出的是杜拉斯小说中“被毁坏的容颜”与叶芝诗歌中“衰老脸上痛苦的皱纹”是两种截然不同的衰老容颜。前者指的是一副因情欲的剧烈消耗，而在短时间内大幅度被毁坏的容颜。后者指的却是顺其自然，在慢长岁月里渐渐衰老的容颜。<br />
<br />
 <br />
<br />
在我看来，《情人》是一部真正的叙述情爱耗减性的伟大小说。其之耗减性是以一张少女之脸被提前几十年的被毁坏为代价的――“十八岁的时候我就衰老了”。《情人》里的少女一直不肯承认她爱过那个中国男人。她从不对他说爱。她要他把她当作任何其他女人。她对他说她只是喜欢和他一起做爱。这是因爱的无望而引起的耗减特性，这耗减在情感、身体等等方面超载的状况下，直接转向了对容颜的伤害。从一开始，白人少女就知道白人社会不会容忍这个黄皮肤中国男子，而黄皮肤的中国富商家族，亦不会容忍一位贫困的白人少女。杜拉斯在后文紧跟着写道：“我常听说我之所以过早地衰老是由于整个童年时期受阳光强烈照射的缘故。但我从来就不相信这种说法。也有人对我说过穷人的孩子爱多思。不过，实际情况并非如此。”是的，实际情况并非如此，实际情况是她的容颜老了，在爱的那两三年被耗减而老，老的一塌糊涂。<br />
<br />
 <br />
<br />
杜拉斯的这种情爱耗减式叙事手段，王小波从结构到语言皆深为神往。事实上王小波的《黄金时代》的结构，就是对杜拉斯《情人》结构的复制。其唯一的区别是将性别角色进行了互换，且叙述者由女性之“我”换为男性之 “我”，情人由中国男子换成了陈清扬。故事构架亦是不可能之爱情，亦是以疯狂的性消耗代替了全部的爱消耗。甚至小说的结尾，陈清扬对王二说“在那一瞬间她爱上了我，而且这件事永远不能改变。”这与《情人》中，中国男人对他所恋的白人情人所说的话如何相似“他说他永远无法扯断对她的爱，他将至死爱着她。”但，这两段相似的话里，令我感兴趣的却是“永远”二字，“永远”在汉语里是个属于时间的词汇。显然“永远爱你”是情人语法，而非夫妻语法。情人语法中，短暂之时在倾其所能的耗减永远（疯狂的做爱），而永远在长久的消费短暂之时（长久的回忆）。正是因了情人之爱的动人，才令短暂之时与永远之时，第一次达到了点与面的伟大和解。</div>

							
							
							
							
