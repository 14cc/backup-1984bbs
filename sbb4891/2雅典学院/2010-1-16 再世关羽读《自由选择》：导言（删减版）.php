													<h2>黄格斯:弗里德曼已逝 自由选择永恒</h2>
						
						
																											<div id="postmessage_435002" class="t_msgfont">　　11月16日，20 世纪最伟大的经济学家之一、自由市场经济的坚定维护者弗里德曼享受完其丰盛的人生后，以 94岁高龄在其旧金山的家中辞世。<br />
<br />
　　华人经济学家张五常曾说，“假若我们说20 世纪的前三分二是凯恩斯及马克思的世界，那么我们可以说这世纪的后三分一是属于弗里德曼的。”这无疑是关于弗里德曼地位和影响的最精辟评论。 <br />
<br />
　　1776 年，现代经济学鼻祖亚当・斯密出版《国富论》，认为建立在个人自由选择基础上的市场经济是人类最理想最完美的制度，管得最少的政府就是最好的政府。此后的一个多世纪中，自由放任思想成为天经地义的信条，被广为接受。不仅如此，斯密的经济人假设还被发展为功利主义哲学，而边际分析、均衡分析等新工具的出现也进一步论证了 “阿罗―德布鲁”式的自由市场的完美无缺。然而，间歇性发生的经济金融危机，特别是 1930 年代席卷全世界的大萧条彻底击溃了人们对自由资本主义的信仰。在德国，纳粹以“民主”的方式掌握了政权，而俄国则在“埋葬资本主义”口号下，“打碎了旧锁链”，引入了中央计划经济，而作为民主资本主义挽救者的凯恩斯主义也开始出现，它以流动性陷阱、消费倾向递减、工资刚性等理论说明自由市场不能解决充分就业问题，因而为政府干预经济提供了理论基础。 <br />
<br />
　　凯恩斯主义在罗斯福新政中的成功以及在战后五、六十年代的普遍流行使绝大多数人相信，政府的普遍干预是维系资本主义香火不灭的唯一途径。就是在凯恩斯主义如日中天的时候，弗里德曼进入为数不多仍在宣扬自由主义的芝加哥大学，他从维纳、米塞斯和哈耶克那里吸取营养，而又极富有创新性，以经济学研究为阵地发动反击，猛烈攻击凯恩斯主义。在《美国货币史》这部划时代的巨著中，弗里德曼论证，大萧条并非是自由资本主义的先天缺陷造成的，而是因为货币当局执行了错误的货币政策。同时，他还论证了扩张性货币政策长期内无效的结论，提出了自然率理论（今年诺奖得主费尔普斯与他大致同一时期提出这一理论），这些理论成为颠覆凯恩斯主义的理论基础。他关于货币量唯一重要的理论，帮助美国政策制定者在1970年代末制服通货膨胀发挥了现实指导作用。如今，与他名字几乎同义的“货币主义”一词已深深刻入当代经济学教科书中。<br />
<br />
　　纯粹的经济学研究绝不会让一个经济学家成为伟大的思想家。弗里德曼以坚实的学术研究为基础，旗帜鲜明地维护自由主义并身体力行。他以专栏作家、演讲者和电视片撰稿人的身份大力宣扬自由选择哲学。他反对政府以充分就业为借口干预经济，反对机会主义的货币政策，反对政府权力扩张和行业管制，反对最低工资制，反对工会，反对企业追求“社会责任”，反对大而不当的福利制度，他甚至主张毒品交易合法化。尽管这些观点具有良好的出发点，比如他论证了为何最低工资制和福利制度最终损害工人和穷人利益，但仍然引起了极大的争议。在 1976年的诺贝尔奖颁奖仪式期间，全球几千名左派人士聚集在斯德哥尔摩，抗议瑞典皇家学会将此奖颁给他。 <br />
<br />
　　尽管弗里德曼谦虚地认为，他的思想对社会影响有限。但是，在战后凯恩斯主义流行20 多年且积弊日深后，他的自由经济思想和个人选择理论逐步被广为接受。1980 年代，在大西洋两岸的美国和英国，分别由里根和撒切尔夫人（她公开宣称是弗里德曼的信徒）执刀，掀起了一场解除管制和非国有化的浪潮，经济活力重新被激发。而此后从东欧到亚洲的范围广大的市场经济浪潮，也无不是自由主义思潮胜利的证明。 <br />
<br />
　　时势造英雄。在自由放任资本主义风雨飘摇的关键时刻，凯恩斯主义应运而生，颠覆了政府不应干预经济的陈见。而当政府势力日益膨胀，已妨碍自由经济，窒息资本主义活力的时候，弗里德曼宣扬的自由选择又获得了空前的成功。从斯密到凯恩斯，再到弗里德曼，正是“否定之否定”规律的一次完美展现。值得注意的是，凯恩斯理论并没有因货币主义的胜利而失败，在重新寻找到微观基础后，它逐渐演化成“新凯恩斯主义”并展现出相当的活力，而货币主义的核心思想被吸收后也在向“新古典经济学”演化。目前，各流派之间越来越多地运用相同的工具和方法，如理性预期、信息不对称理论。这一趋势所折射的，或许正是在现代经济对自由市场和政府调节的双重需求。&nbsp;&nbsp;<br />
<br />
　　中国近20年来的巨大成功，同样是市场经济力量的又一次证明，是经济自由和个人选择的一次伟大胜利。中国有着深厚的干预主义传统，在改革转型的过程中，市场化改革不足，以公共利益为名行垄断和管制之实的现象仍普遍存在，此时重温弗里德曼关于自由选择，关于个人与政府关系、个人与个人关系的思想，仍具有震聋发聩的作用。同时，我们也应注意到，弗里德曼的个人选择理论，并未给社会公正以应有的地位，或者说他的自由选择理论建立在公正已存在的假设之上，而正向市场经济转型的中国，则面临着引入自由经济和社会公正的双重任务。在这种形势下，防止自由选择思想的误用和教条化滥用很有必要。<br />
<br />
　　政府、自由市场与个人之间的矛盾和冲突，是现代社会发展中的一个恒久命题，它不会因弗里德曼的去世而消失，相反，某些时候它还会以剧烈冲突的形式表现出来，从这个意义上看，弗里德曼所留下的遗产具有永恒的价值。</div>

							
							
							
							
