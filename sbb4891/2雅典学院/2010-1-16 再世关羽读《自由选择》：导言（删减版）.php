													<h2>弗里德曼：自由选择</h2>
						
						
																											<div id="postmessage_435012" class="t_msgfont">　　诺贝尔经济学奖得主、美国经济学泰斗米尔顿・弗里德曼16日在旧金山去世，终年94岁。<br />
<br />
　　弗里德曼是20世纪最有影响力的经济学家之一，有人说，自他之后，经济学大师时代可能已经一去不复返了。他的货币理论影响了尼克松以来3届美国总统的经济政策。在国外，他提出的政府不应过度干预经济运行的自由市场理论已被发展中国家政府普遍接受。<br />
<br />
　　犹太移民后裔 童年生活贫寒<br />
<br />
　　弗里德曼1912年7月31日出生于美国纽约市的布鲁克林，是俄罗斯犹太移民后裔。父亲开了家小成衣厂，母亲在纽约一家雇用廉价劳动力的工厂当裁缝。没过几年，由于经营不善，小成衣厂关闭了。后来，弗里德曼的父母又开了一家纺织品店。在此后的大多数时间里，都是母亲在经营这个商店。让弗里德曼记忆犹新的是，父母总是在夜间讨论如何弄到钱来支付以后的账单。而且这种状况一直持续了很长时间。这主要是因为:无论是小成衣厂、纺织品店，还是在弗里德曼11岁时家里开的冷饮店，利润都是微乎其微，只能够勉强维持全家人节俭的生活。<br />
<br />
　　弗里德曼15岁时，父亲就去世了，几乎没给家里留下什么钱。<br />
<br />
　　后来，弗里德曼得到一笔奖学金进入罗格斯大学，他用在学校附近的餐馆当侍应生和在一家百货公司当收银员的收入补足不够的学费。1932年毕业时，他获得经济学和数学双学位。那一年，正是美国经济大萧条中最黑暗的日子。<br />
<br />
　　毕业时，弗里德曼同时获得布朗大学(数学)和芝加哥大学(经济学)提供的奖学金。最后，他选择了芝加哥大学――这个有着全美最出色的经济系的大学，继续深造。当时的芝加哥大学里，有一批经济学家并不认同整个经济学界的看法，认为大萧条的发生与不可避免的资本主义危机二者之间并无必然联系。这对弗里德曼经济思想的形成产生了相当的影响。<br />
<br />
　　1933年，弗里德曼获得芝加哥大学经济学硕士学位。1946年，弗里德曼开始在芝加哥大学经济学系当教授，直到退休。<br />
<br />
　　挑战学术权威 一度饱受争议<br />
<br />
　　如果评选两名２０世纪美国最知名经济学家，最有可能入选的就是凯恩斯和弗里德曼，两者各领风骚５０年，但弗里德曼的经济学理论与凯恩斯恰恰背道而驰。<br />
<br />
　　20世纪50年代，弗里德曼开始宣扬“自由市场经济”，强烈批评政府干预市场，这与当时被人们奉为经典的凯恩斯主义格格不入。凯恩斯认为，出现大萧条的原因是由于美国经济的私有化性质，换言之，是资本主义经济危机的必然产物，因此不仅凭私有经济本身的力量，并不能走出景气循环的低谷，相反，还会越陷越深。凯恩斯由此提出了政府干预经济的理论。弗里德曼则认为造成美国20世纪30年代经济大萧条的原因，与凯恩斯所提出的恰恰相反，并非是美国私有化经济所造成的，而是美国政府过于干预经济运行规律的过错。<br />
<br />
　　弗里德曼最著名的是他的“货币数量化理论”，即通货膨胀源于“太多的货币追逐太少的商品”。一旦政府接受这一理论，就可以通过放慢货币增长率来控制通胀。<br />
<br />
　　１９６８年，弗里德曼又提出“自然失业率”理论。此前流行的凯恩斯理论认为，在稳定的经济运行中，失业和通胀可以共存，政府可以选择自认为最合适的失业和通胀比例。但弗里德曼指出，如果政府试图人为压低失业率，通胀会上升更高。<br />
<br />
　　因为挑战了凯恩斯理论的权威，弗里德曼曾经饱受美国经济学界排挤。那时，弗里德曼的经济学思想被视为荒诞不经，像杜克大学那样的名校图书馆中，弗里德曼的著作甚至不摆放在书架上。<br />
<br />
　　但弗里德曼坚信自己理论的正确性，他随时随地与他人展开辩论，受尽白眼仍毫不气馁。随着他的理论一一在市场上得到验证，到上世纪七八十年代，世人终于意识到他对经济学的贡献，承认了他的历史地位。<br />
<br />
　　布什总统２００２年曾这样评价弗里德曼：“在他看来，一个理想的社会是：个人有选择自由，而政府没有无视个人选择的自由。”“这种洞察力改变了美国，而且正在改变世界。”<br />
<br />
　　美联储主席本・伯南克说：“在经济学者中，米尔顿・弗里德曼独一无二。”<br />
<br />
　　获得诺贝尔奖 与凯恩斯齐名<br />
<br />
　　1976年，弗里德曼获诺贝尔经济学奖，当他在斯德哥尔摩接受诺贝尔奖时，会场上出现了不和谐的声音，有示威者高呼口号，反对他得奖，最后被驱逐出场。<br />
<br />
　　时代不同了，数十年后，历史终于向这位经济学伟人低头，承认他与凯恩斯齐名。弗里德曼几乎是个人赤手空拳地让这个世界承认了“货币数量化理论”。<br />
<br />
　　从上世纪70年代开始，弗里德曼从芝加哥大学的教学工作中退下来，搬到旧金山。显然，他很享受他的晚年生活。旧金山气候宜人，他的工作地点――设在斯坦福大学的胡佛研究所离他的寓所也很近。<br />
<br />
　　在经济领域之外，弗里德曼还是自由主义思想的不懈倡导者。他的著作《资本主义与自由》(1962年)影响很大。在该书的导论中他写道，“对自由的严重威胁在于权力的集中。对于维护我们的自由而言，政府是必要的……但将权力集中于政府手中，也是对自由的威胁”。<br />
<br />
　　弗里德曼善于推销自己的经济学理论，他笔耕不辍，撰写多部著作，还从１９６６年至１９８３年间，长期为《新闻周刊》撰写专栏，在枯燥的经济学理论和公众之间架起理解桥梁。<br />
<br />
　　除了《资本主义与自己》，弗里德曼最有名的著作还包括，《价格理论》（１９６２年）、《一个经济学家的抗议》（１９７２年）、《没有免费的午餐》（１９７５年）等。<br />
<br />
　　婚姻幸福美满 人生之“幸运”<br />
<br />
　　1938年，弗里德曼与罗斯结婚，这位同样是经济学家的女性聪明睿智，是丈夫多本有名著作的合著者。晚年弗里德曼在回忆录《两个幸运的人》中，将自己的成就归因于“幸运”，归因于美国社会的自由开放、学生时代的良师益友、自己所经历的时代巨变等等。当然，相濡以沫的爱侣罗斯在70多年中的相依相伴也是弗里德曼取得巨大成就的“幸运”之一。<br />
<br />
　　弗里德曼在回忆录中写道：“我们两人都是大学毕业后又进研究生院继续学习，我们各自从北美大陆的两端来到同一所研究生院。6年后我们结婚，这时我们已分别开始了作为经济学家的职业生涯。我们幸运地拥有长久而美满的婚姻，至今已经相濡以沫地在一起生活了 59年，有着两个出色的子女，4个孙辈，还拥有有所作为的职业生涯，其中的多数时间我们是合作者。”<br />
<br />
　　“虽然我们两人受的都是经济学教育，但是我们彼此之间从来都不是竞争者。罗斯一次又一次地被问及我们长久而幸福的婚姻的‘秘诀’，每到这时她总是回答，我们有着共同的兴趣，但是性格和品位各自不同，我们总是努力适应对方。宽容是家庭生活成功的秘诀，一如它是在社会走向成功的秘诀。”<br />
<br />
　　曾经三次访华 中美交流缩影<br />
<br />
　　弗里德曼三次对中国的访问，几乎可以说是当时中美之间交流的缩影。<br />
<br />
　　1980年、1988年、1993年，弗里德曼三次来华访问。他在自传中写道：“对中国的三次访问是我们一生中最神奇的经历之一……”<br />
<br />
　　1980年的访问是唯一由官方正式邀请的访问。邀请者是中国社科院世界经济研究所。邀请者希望弗里德曼就世界经济、通货膨胀、计划经济社会中市场的运用等问题发表演讲。这时的中国，改革的进程刚刚开始，大多数人对于未来要走的道路几乎一无所知。<br />
<br />
　　1988年，弗里德曼第二次访华。这次访华是三次中最重要的一次，这不仅仅是由于弗里德曼见到了当时中国的主要中央领导人，而且由于访问的范围更大，弗里德曼得以对中国有了更深入的观察和了解。同时，弗里德曼的自由经济观点，在当时中国改革持续深入情况下得以更清晰地传递给了中国的决策者和学术界。<br />
<br />
　　1993年，弗里德曼第三次访华。这时的中国改革，在经历了反复后重新开始快速发展。除了北京和上海以外，弗里德曼还访问了成都和重庆等地。私营企业、民间商业的发展给弗里德曼留下了深刻印象。<br />
<br />
　　在三次访华中，弗里德曼近距离地观察了一个从计划经济体制走向市场经济体制的社会的变迁过程，他尽力向中国人介绍了他关于自由市场制度的知识，表达了自己的思想观念。作为一个经济学家，他深入影响了一大批中国人――其中不乏能够直接影响甚至决定中国发展道路的人。可以说，在这十几年中，弗里德曼原有的在世界范围内的影响力扩展到了中国，而中国，也因此受益良多。<br />
