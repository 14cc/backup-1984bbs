													<h2>再世关羽读《自由选择》：导言（删减版）</h2>
						
						
																											<div id="postmessage_434205" class="t_msgfont">今天开始读弗里德曼的《自由选择》一书，并不定期分享自己的读书心得给大 <br />
家，希望大家多多指点。<br />
<br />
作为芝加哥学派的代表人，弗里德曼的主要观点是坚持自由市场经济，反对政 <br />
府干预。应该说，这种经济指导思想是正确的，然而，对于目前中国来说，并 <br />
不实用。中国的经济问题，恰恰是中国政府在“好的”愿景下（动机有为国家发 <br />
展，也有为自身利益）制定的大量错误经济政策（由于其社会主义指导思想与 <br />
权贵资本主义经济现实的冲突，及政府官僚机构中经济人才的极端匮乏，这些 <br />
经济政策几乎没有产生任何正面影响）而导致的整体经济畸形化发展，为日后 <br />
的经济危机埋下了巨大隐患。换言之，我国的经济问题，是由政府一手造成的 <br />
。因此，面对这样的背景下不加以分析的采用全面开放市场的经济改革方式， <br />
无疑是误解了芝加哥学派的理论，我认为，前政府经济政策带来的经济危机， <br />
需要新政府运用政权的力量进行修正，将经济引入正轨后方可进行经济开放， <br />
实现全面的自由市场经济，否则的话无疑是将政府制造的烂摊子扔给民众来解 <br />
决，无疑是很不负责任的行为，并且具有极大的危害性――美国胡佛政府面对 <br />
大萧条的无所作为和俄罗斯休克疗法带来的恶果便是明证。<br />
<br />
我今天所读的只是《自由选择》一书的导言。在导言中，弗里德曼介绍了美国 <br />
所创造的历史奇迹的经济与思想根源――来自亚当斯密《国富论》中的自由市 <br />
场理念与《独立宣言》中的“人人有权追求其自身价值”理念。<br />
<br />
然而，这恰恰说明在我国，直接过度到自由市场经济是不明智的行为。我们国 <br />
家历史上从来没有产生过真正意义上的自由市场，我们的国民也从来没有接受 <br />
过《独立宣言》式的自由平等理念。在今天，虽然人民有了一定的权利意识， <br />
但在经济上，他们依然深受共产主义经济理念的影响，认为政府应当来主导经 <br />
济。换言之，他们并没有接受自由市场经济的思想基础，也很难快速适应中国 <br />
经济转型为自由市场经济后所带来的观念与经济行为的巨大转变。<br />
弗里德曼认为，经济自由是政治自由的必要条件。自由市场可以有效化解政治 <br />
集权，而相反，如果经济政治权力都集中在某一个或一群人手中――很不幸， <br />
这正是我们国家的政治体制，那么必然导致专制，暴政（他又不幸地说中了） <br />
。事实上，只要共产党继续掌握政权，他们断然不会全面开放市场经济（除非 <br />
他们大脑集体短路）。<br />
<br />
弗里德曼认为，创新的主要源泉是自由市场下的个人主观能动性，而创新又是 <br />
生产力提高的根源。后一句我完全同意，前一句我认为是不全面的，在奴隶社 <br />
会，失去人身自由的奴隶们依然可以创新出精美的工艺品和所谓的“奇迹”，但 <br />
这些根据奴隶主意愿而来的创新是不符合甚至背离生产力发展的要求的，所以 <br />
无助于社会生产力水平的整体提高。而每次社会制度的大变革带来的生产力革 <br />
命，又说明了只有在（相对）自由人为了改善生活水平的主观能动性而产生的 <br />
创新才能有力的推动生产力的发展。我认为这也解释了为什么在中国这么个经 <br />
济制度依然专制的国家里，生产力水平仍然不断提高。<br />
<br />
弗里德曼又认为，经济与政治自由带来的成功使他们对思想家的吸引力减少。 <br />
人们越发认为政府的角色应该是“家长”，政府的权力应该是强迫一些人去帮助 <br />
另外一些人。我认为，这种思想的产生于人性中愿意推诿责任的惰性有密切关 <br />
系。然而讽刺的是，正是这种思想带来了人类历史上最“无私“，也是最邪恶的 <br />
共产主义。<br />
<br />
无论资本主义国家还是社会主义国家，政府普遍陷入了一个可怕的误区（只不 <br />
过社会主义国家陷得更深，后果更加惨烈）,”政府们出于增进个人利益的美好 <br />
愿景，打着保障，平等的旗号从一部分人手中拿出钱来转发给另一部分人“。 <br />
这样做的不但会倒是生产率的低下（这种势头在北欧福利国家尤其明显）进而 <br />
毁掉自由市场机制带来的繁荣，更会导致政府向集权化方向发展，进而进一步 <br />
的侵犯人权。<br />
<br />
事实上当今中国的情况要严重的多，中国政府“打着保障，平等的旗号从一部 <br />
分人手中拿出钱来转发给另一部分人”的目的并不是为了维护个人利益，而是 <br />
为了维护自身的统治地位，这使得其种种“福利”行为政策（如社保，医保等） <br />
具有很大的随意性与不透明性，且贪污挪用现象极其严重。更糟糕的是，为了 <br />
统治阶层的利益，他们“劫富济贫”的对象并非与他们利益息息相关富豪阶层， <br />
而是广大中产阶层与城市的中低收入者。这无疑大大加剧了中国社会与经济的 <br />
风险。而且，在美国等西方国家，很多有识之士已经认识到了“大政府&quot;的危害 <br />
，并以舆论为武器转变百姓意识，说服民众依靠主观能动性而不是集体主义来 <br />
促进自身发展。然而，在集体主义对百姓影响深远，且主要舆论管道（媒体） <br />
控制在中共政府手中的中国，依靠舆论武器来转变社会意识的想法无疑是不切 <br />
实际的。如何促进中国经济向自由市场化发展，我们确实任重道远。<br />
<br />
以上就是我对《自由选择》一书中”导言“的一点心得。希望大家能够指正。我 <br />
会争取在三到五天内阅读完一章内容并不定期发表我对《自由选择》的心得。 <br />
欢迎大家指点<br />
