																											<div id="postmessage_597638" class="t_msgfont">文/亦忱<br />
<br />
&nbsp; &nbsp; 一、<br />
<br />
&nbsp;&nbsp;我因为经常前去上海探视老师和老领导俞老炳林先生，加之从08年秋天起，有一起本人的著作权被《东方早报》侵权的民事诉讼发生在上海，而多次进入过上海的政府机关和两级法院进行相关交涉。因此，我对上海各级党政部门的安保措施印象极为深刻。<br />
<br />
&nbsp;&nbsp;在我看来，自从杨佳袭警案发生之后，上海的党政机关和各个政法部门的门禁规格，大概与我所去过的国内外各大机场登机之前的安检几乎一般无二了。别说你若想携带一把可以杀人的刀具进入这些机关已经难如登天，你哪怕就是想带一瓶饮料进去，门卫也得要你当面大喝一口才成。如今，任何想在上海的党政机关和政法部门制造恐怖活动的中国暴徒，铁定不能再次取得杨佳那样的功效了。<br />
<br />
&nbsp;&nbsp;然而，全国各地的小学和幼儿园，恐怕是没有条件都变成固若金汤的城堡。所以，国人都看到，自今年3月23日起至4月30日止，从南到北，全国共发生了5起具有中国特色的恐怖分子针对最无辜、最弱势、最无防卫能力的幼童施加的暴力犯罪事件。<br />
<br />
&nbsp;&nbsp;我想，任何一位自称关心祖国未来的人，如果你不关心这些个令人揪心和切齿的残暴犯罪现象，及其隐含在其中的发生与发展的内在因由，那我基本上可以下结论：你大概就是一头真正的中国猪，而没有资格称为中国人。<br />
<br />
&nbsp;&nbsp;当年，早在杨佳袭警案发生之前三年的2005年9月11日，在北京王府井大街上，曾发生过一起艾绪强抢劫出租车驾车滥杀无辜的报复社会杀人案，共造成3死5 伤。虽然在随后展开的司法程序中，杀人犯艾绪强被处死了，但导致这个丧心病狂的绝望者拉人垫背自杀的原因，却并未进入我们这个社会进行公开检讨的范畴。<br />
<br />
&nbsp;&nbsp;短短五年过去了，中国那些制造校园杀童案的恐怖分子，虽然都没有达到读懂那本阿拉伯恐怖分子的教科书《超限战》的文化水平，但他们却凭借中国人特有的天分，无师自通地知道，任何恐怖活动的功效之一，都是冀望收获具有全球性关注的轰动效应和整个人类社会最大、最强烈的仇恨。<br />
<br />
&nbsp;&nbsp;非常遗憾的是，我们大家都看到了，无论是南平杀童的暴徒还是泰兴杀童的暴徒，也无论是雷州杀童的暴徒还是合浦杀童的暴徒，他们确实都做到了这一点。<br />
<br />
&nbsp;&nbsp;二、<br />
<br />
&nbsp;&nbsp;在这些令人发指的杀童案接二连三发生之后，北京学者许志永在其个人的博客上，写了一篇题为《悲剧的意义――中国特色的个人恐怖主义》的文章，对这些个令人揪心和切齿的具有中国特色的悲剧作了虽说浅尝辄止，但也可说是迄今为止最有思想深度的解读。<br />
<br />
&nbsp;&nbsp;许志永在文中这样写道：“每个国家都有个人恐怖主义这种社会现象。作为一种社会事实，该现象有五个主要构成要素：社会问题――没有完美的社会，总会有贫富差距、特权腐败、社会歧视、缺少关爱等等问题;主角――个性偏执的杀人者，但从更广泛的社会意义上说，主角是谁并不重要，每个人看到的社会都是自己的偏见，社会问题的信息在某个人头脑中积聚直至爆发，本身具有社会性，杀人者本身具有某种公共象征意义;受害者――无辜的弱者，在整个事件的客观意义上，受害者作为个体并不重要了，重要的是作为无辜的弱者，当然在杀人者看来，社会问题没有无辜者;震撼性――事件具有可传播性并且具有震撼性的广泛社会意义;社会整合 ――震撼性的事件作为警醒，强化某种社会共同体的共识。<br />
<br />
&nbsp;&nbsp;对导致这些发生在中国社会的恐怖事件的因由，许志永认为：“以个人恐怖主义五个要素来分析：社会背景――拆迁、社会保障缺失、社会不公等等，这些问题在有的人的头脑里聚集并强化;主角――个性偏执的个人，但如果连续出现极端个体，这就说明社会问题已经相当严重;受害者――社会中最弱者，需要指出的是，一个人能够把行凶对象指向幼儿园的孩子，这需要巨大的（精神）力量给自己内心填充 “合法性”，这些力量来自社会问题的各种信息;震撼性――该事件成为一个巨大的公共事件具有内在必然的逻辑;社会整合――如果一个社会回避巨大公共事件的深刻含义或者失去了整合的能力，该社会事实的意义可能是强化某种本已存在的失落情绪，这是悲剧中的悲剧。”<br />
<br />
&nbsp;&nbsp;在叙说了2007年美国弗吉尼亚大学校园内发生的赵成熙滥杀同学而导致33人丧生的枪击案及其事件的善后结果之后，许志永这样写道：导致赵成熙杀人的理由，或曰社会问题，是所谓凶手缺少关爱和弥漫于校园内的享乐主义令他感到极端仇恨，“也许这个问题在很多人看来并不严重或者说可以容忍，但这确实存在，并且没有引起足够的重视。主角是赵承熙，他沉默寡言个性偏执，社会问题‘选择’了在他头脑中聚集并强化，他决心以生命殉他内心的道――他在寄给NBC电台的录像带中批评享乐主义‘有奔驰车还不够吗?有金项链还不够吗?所有这一切欢乐还不够吗?’受害者是他的同学，无辜者，而在杀人者看来，在这个普遍联系的世界上每个人也都有责任。通过媒体传播，该事件极具有震撼性，也就具有了广泛社会意义的能量。该事件的结果或者说意义，赵承熙和被他杀死的同学一样也是被悼念者，整个社会（因此）增强了关爱。”<br />
<br />
&nbsp;&nbsp;三、<br />
<br />
&nbsp;&nbsp;据传，制造美国惊天枪击案的罪犯赵成熙死后，人们在他的绝笔中，发现了如下的文字：“知道有人往脸上吐唾沫的滋味吗？知道把垃圾从嗓子里咽下去的滋味吗？知道自掘坟墓的滋味吗？你们让我痛苦，自己却很快乐，为了你们的快乐，我就像脑袋里长了癌一样痛苦，心脏四分五裂，到现在还在撕咬我的灵魂。”<br />
<br />
&nbsp;&nbsp;也许，具有澄明道德情怀的美国人在事后换位思考的基础上，真正理解了杀手赵成熙的绝望和疯狂，不光光是他个人的性格病态所成就，其中，与杀手朝夕相处的师生、校园乃至整个美国社会都是有责任的，所以，后来全世界所有人都看到，美国人在追悼那32名死于这个穷凶极恶的杀手枪下的冤魂之时，一道祭奠了这个毁于自己枪下的十恶不赦的暴徒，学校在追悼仪式上放飞的悼念气球不是仅仅给那32个冤魂，而是放飞了33个气球，其中有一个是给这位惊天杀手的。尤其令人深感震撼的是，据说，杀手和死于杀手枪下的无辜者，竟然被集体安葬在同一个墓园之内。<br />
<br />
&nbsp;&nbsp;以此反观我们中国人对那些深陷人生绝望境地，而凭借制造恐怖暴力事件走进公众视野的兄弟姐妹们，他们却无一不是作为人类的渣滓，被主流社会像摁死一只蚂蚁一样，在极短的时间内被法院处死。马加爵、王斌余、艾绪强、杨佳、郑民生，其中，有哪一位深陷人生困境之中而制造出惊天血案的同胞，在事后被中国的学术界公开作为个案研究过？我们这个民族的政治精英和文化精英，又有谁曾设身处地为这些个同自己一样生存于这个时代却死于非命的人，给予过丁点的同情呢？<br />
<br />
&nbsp;&nbsp;如今，中国从南到北，从东到西，每个大中城市都在精心加强对校园的保卫，以防更多的暴徒依样画瓢，照着郑民生首创的恐怖行为加害无辜的学童。这固然是必要的举措。<br />
<br />
&nbsp;&nbsp;然而，那些潜在的郑民生们，其头上并未刻上暴徒的字样，他们若想以生命作代价来制造一起轰动性的恐怖事件，中国的战略家们其实早已经为他们编写了极好的教材《超限战》。所以，我对这种防范中国式的个人恐怖活动的预后前景并不看好。<br />
<br />
&nbsp;&nbsp;我以为，我们只能深入到那些中国式的恐怖分子内心，去探寻这些深陷人生绝境的人，为何会产生加害无辜者的念头，然后，再对症下药，去改变他们心跳的方式，进而改变他们的行为方式，我们这个社会才能从日益走向暴虐，而改弦更张，日渐走向和谐与安祥。<br />
<br />
&nbsp;&nbsp;后记<br />
<br />
&nbsp;&nbsp;近日，我去北京探访几位好友之余，抽空去京郊50公里之外的廊坊，看望了网友中条山人大哥。<br />
那夜，我们在吃过晚饭之后，中条山人领我去看了一个濒临被强拆命运的社区。这个社区住了近千户人家，所有住房均建于12年之前。只是由于廊坊的主政者要实现“三年城市大变样”的宏伟施政构想，他们就得无条件地搬家走人。<br />
<br />
&nbsp;&nbsp;虽然平心而论，我看过了廊坊市政府拆迁这个社区的补偿条件，确实也感到，这个拆迁条件真能算我所看到过的最公道的拆迁条件之一，但是，那位身上少了5根肋骨并罹患心脏病和高血压，已年届74岁的杨大妈声泪俱下地对我诉说道，自己恐怕是留不到一条老命，在两年后再返迁进新居的话语，确实是令我心头遭到重重的一击。<br />
<br />
&nbsp;&nbsp;据杨大妈的两个女儿告诉我，他们实在是很难理解，自己住得好好的宅子，产权证和户口本俱在，凭什么领导动个念头，说“三年城市大变样”，我们就得带着奄奄一息的老娘，而流离失所呢？<br />
<br />
&nbsp;&nbsp;我不晓得，我最后所讲的这个故事，当下中国那些有责任维护这个社会秩序不被恐怖分子破坏的人，听了之后又会作何感想呢？<br />
<br />
