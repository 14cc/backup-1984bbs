知道分子[王朔]<br />
<br />
　　有一孩子，从小爱学习，人也不黏不傻，老师讲什么，家长讲什么，社会上闲杂人等讲什么，孩子听了都往心里去。后来认够了字，也比比划划会写了，见书就看，拿小本子就抄，历代名人的胡说，招三不招两的话，只要话够大，理想啊，生命啊，都记。知道的是抄别人的话，不知道的还以为孩子自己爱想问题，人见人夸：这孩子出息。夸得多了，孩子自己也觉得这叫出息了，越发不可收拾，小学，中学，大学，硕士，博士，博士后，一念就是二十多年；然后摇身一变，助教，讲师，副教授，教授，博导，又是二十多年；俩二十年一加，五十多年；再加上前面还有六七年不懂人事的岁月，孩子奔七十了。 <br />
　　以为孩子这一辈子白过了？孩子一天没闲着，除了把中国字认了一溜够，一闭眼好几万字，外国字也认了十几门，一门结结巴巴能说的，两门扳着字典能读的，三四门看着眼熟，五六门会说“哈喽”。这孩子还了得吗？可天下的事什么他不知道？可天下的人但凡有一号的都是他熟人，特别是死了的，越死得时候长，越跟大伙没关系，孩子就越熟。孩子的心和他们是“相通的”。 <br />
　　仗着这帮死人，孩子开始教训活人。只有他知道死人说过什么。孩子门儿清“太阳底下没有新鲜事”，不是光咱们这一辈人好议论爱想事，早八辈子的人都是这么爱说爱琢磨事，是道理，都让死人说遍了，全是现成的，重抄一遍就是了。什么叫“彪炳千秋”？什么叫“万古长青”？就是一千年一万年前说对，一千年一万年后再说，还对!――这千万年当中，大家就闭嘴吧。印第安人有一个信仰，认为每个死去的人都会给世界留下一个生命的纪念，一缕头发，一颗牙齿，甚至一滩粪便，以传达他了解的知识，也是个保佑后人的意思。中国读书人也有这么一个信仰，孩子就是他们留下的头发、牙齿和粪便，当然他们不这么叫，叫“读书种子”。有孩子在，不肯死或不甘心的读书人就觉得留了一点东西给后人，就觉得自己没全死而快乐了。 <br />
　　和伟大的人搞惯了，有一个问题，就是以为自己也很伟大，或者他老大，我老二。抄惯了别人的宏论，也有一个问题：不知道哪句不是自己的。其实这很容易分辨――哪句也不是你的。 <br />
　　第一个人说的，叫“知识分子”。第二个，第三个，还有不知道隔了多少代隔了多少辈，俗称“八杆子打不着的”，都叫“知道分子”。 <br />
　　附注：分辨“知道分子”小常识：写伟人传记的；为古籍校订注释的；所有丛书主编；所有“红学家”和自称鲁迅知己的。? <br />
　　次一等：好提自己念过多少年书的；死吹自己老师和老老师的；爱在文章里提他不认识的人和他刚看过的书的。</div>

							
							
							
							
