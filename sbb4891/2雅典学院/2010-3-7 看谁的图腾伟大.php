																											<div id="postmessage_506414" class="t_msgfont">――对知识界的人类学观察<br />
<br />
　　作者：郭宇宽<br />
<br />
　　1,<br />
<br />
　　我很喜欢人类学,读人类学的书,常感到一种像小时候读&lt;辛巴达历险记&gt;或者<br />
&lt;尼尔斯骑鹅旅行记&gt;一样求知和探险的气质。<br />
<br />
　　不过后来又发现西方为背景的人类学研究，其实常常也有掩盖不住的偏见，<br />
他们常常把观察东方社会的研究称为人类学，在他们的潜意识里，比如中国，印<br />
尼，或者印第安人，还有亚马逊丛林里的食人族，都是被归为和他们不一样的很<br />
奇怪的存在。他们研究西方自己的社会，通常就被称作社会学。<br />
<br />
　　大概所有的人类都避免不了以自己为中心的意识形态。中国历史上所有对于<br />
外部世界的描述，往往也带有偏见，我们的祖先对于“蛮夷”的描述，和近代对<br />
于“洋人”的描述，也和一些早先“洋人”对我们的描述是差不多的。<br />
<br />
　　如果翻译成彼此的文字，常常会觉得很受伤害，比如印第安人通常会很不喜<br />
欢一个白皮肤欧洲人对他们的人类学观察，觉得那完全是偏见，对他们伟大的传<br />
统充满偏见；中国人也常常不会喜欢一个西方人对自己文化的人类学描述，觉得<br />
他们有一种居高临下的态度，缺乏对我们传统的尊重，粗鲁的作出概括，却并不<br />
真正理解中国人的内心世界。<br />
<br />
　　这种信息不对称是不可避免的。而人类学的妙处，就恰恰在于这种作为“他<br />
者”的观察视角，无论西方人还是东方人，描述自己和自己熟悉的社会的时候，<br />
即使在潜意识里，也很难避免一些预设的干扰，这些预设可能是理论性的，可能<br />
是信念性的，甚至是出自情感的，这些都会干扰我们的判断。<br />
<br />
　　人们常常会很敏锐地感知到别人对自己有“偏见”，其实人们对自己的“偏<br />
见”只会更大，佛教中叫“我执”，不过只有自省的意志极强的人才能体会到而<br />
已。<br />
<br />
　　2，<br />
<br />
　　所以在我看来，最精彩的研究，是能用人类学的态度来研究自己和自己熟悉<br />
的社会，这样既能够避免“我执”，又能避免信息的不足。<br />
<br />
　　比如有一些当代的杰出人类学者，以在西方受教育的背景，回到他最初成长<br />
熟悉的环境，比如一个小山村作研究。他的观察既比完全不了解那个环境的纯粹<br />
“外国佬”要深入，有比从来没有出过门对那种生活已经习以为常的人更加敏锐。<br />
<br />
　　在我生活中也有这样的体会，有的时候，我在和一群人吃饭或者卡拉OK,举<br />
座都是闹闹哄哄的，会突然觉得自己好像飘到了半空在俯视包括自己在内的这么<br />
一群人，不知道这是不是就是传说中的“元神出窍”。这种状态极为有趣，那个<br />
“我”在观察着一个叫郭宇宽的人，和别人真真假假的应酬，有些表现非常可笑，<br />
每个人都很好玩。这种快乐只有作为一个旁观者才能体会到，就好像我们去动物<br />
园，看到那些老虎猴子们的各种举动，觉得真好玩，不过它们自己每天就是这个<br />
样子，并不觉得好玩。<br />
<br />
　　不过这种状态稍纵即逝，很难捕捉。<br />
<br />
　　3，<br />
<br />
　　我是一个知识分子，我的志业就是做一个知识分子，我的朋友也很多都是知<br />
识分子。知识分子在我眼中是一个崇高的词，它代表着很崇高的使命，知识分子<br />
应该就像猫头鹰一样，在天黑的时候趴在树枝上，睁大眼睛，它能看见别人看不<br />
见的东西。<br />
<br />
　　如果有人在我面前侮辱整个知识分子群体，我会听了觉得很不舒服。不过最<br />
近两次“元神出窍”的经历，让让我有一些超越的体现，发现把知识分子的群体<br />
其实也可以当作一个个部落里的原始人来观察。<br />
<br />
　　一次是个几个年轻的学者，一开始讨论的大概是土地制度的问题，好像是在<br />
比如如何看到集体所有制的问题上有不同的观点，有一个一开始很平静的伙计越<br />
来越激动，他的立场有些“中国新左派”色彩，不过看得出来他是很真诚的，他<br />
是支持农村土地集体化的，我倒很期望听到能从他那里能听到什么新颖的论据或<br />
者论证，不过他在现场好象并没有讲出什么让人觉得有说服力的东西。他突然冷<br />
不丁来一句，你知道伯恩施坦嘛？我说知道啊。老实说，相对于其他左派思想领<br />
袖，我对伯恩施坦还是有一些好感的。他接下来一句，伯恩施坦的原著你都读过<br />
么？我只好坦白，确实没有看过。我甚至一本都没有看过，只是在一些思想史的<br />
书中看过一些概括性简介而已。他脸上立刻流露出胜利的轻松，表现出不用再和<br />
我讨论了的态度，直接起身就告辞了。我们其它几个人都面面相觑，我们讲一个<br />
土地集体化的问题，能把事实是什么，逻辑是什么讲清楚就行了，不至于非得把<br />
博恩施坦的原著都看过一遍才能张嘴讨论吧。<br />
<br />
　　前一天又遇到一次，几个学界友人吃饭，席间有一个“坚定的自由主义者”，<br />
我们说到了一个什么问题，他提出一个观点，中国历史上的法家思想儒家思想都<br />
是专制思想，尤其孔子的思想是中国专制思想的根源。当然有这个观点的人也是<br />
比较普遍的，不过我比较较真儿，谈了谈我的看法，从一些基本历史事实的考证<br />
来说，很难得出结论，儒家站在专制的一边，尤其是在秦始皇建立大一统之前，<br />
儒家更是典型的封建体制而不是集权体制的维护者，封建和专制是两个截然不同<br />
的概念，不可混淆。结果这个哥们毫不从事实和逻辑的角度回应，突然冒出一句<br />
“我是波普尔的信奉者”，对波普尔我恰巧还算比较熟悉，但也在纳闷，波普尔<br />
和孔子有什么关系？正在我迟疑的这一会儿，他骄傲地宣布，我讲的东西和你不<br />
在一个层面，你讲的东西我根本听都不想听。我还奇怪怎么自由主义者也有这么<br />
粗鲁的，他非常自豪“我的性格和波普尔是一样的，我讲的东西你是理解不了<br />
的。”这一招最厉害，一千个知识分子心目中可以有一千个波普尔，除非能把波<br />
普尔本人拉到饭桌上来做做裁判，看谁更理解他。如果一个人抢先一口咬定“你<br />
就是不理解波普尔！”也不讲出具体的道理，还真是无法辩解。<br />
<br />
　　我后来一度曾猥琐地想，以后我得从自己熟悉的经验里面找出几个比较冷僻，<br />
了解的人不多的理论体系，比如我以后假如和别人讨论问题，可以抢先发问，<br />
“因明学的东西，你都研究过么？”或者“卢曼的书，你都读过几本？”乘对方<br />
愣神的时候，就可以宣告我胜利，“靠！连这都不懂，我讲得东西你肯定理解不<br />
了，回家读读书再来和我讨论吧。”要是再毒一点，假如碰巧有人说也研究过因<br />
明学，我就问他“《取事施设论》看过没有？”他肯定傻了。我接下来就可以挖<br />
苦他，“《取事施设论》都没看过，还好意思说懂因明学？”他肯定无地自容，<br />
我就不战而屈人之兵了。<br />
<br />
　　4，<br />
<br />
　　这决不是两次孤立的经历，我所谓的“知识圈”旁观过很多类似的景象。<br />
<br />
　　我过去一直对自己讲道理的能力非常自信，只要我想清楚的道理就一定能说<br />
清楚。如果遇到需要辩论的场合，在我的经验中我几乎是无敌的。 <br />
<br />
　　但最近一两年我的自信很快就荡然无存，在遇到一些甚至比我更年轻的朋友<br />
的时候，我都经常落荒而逃。比如北京的思想界有很多活跃的小圈子，在BBS,邮<br />
件讨论组里讨论各种问题。年轻人嘛，经常发生争执，也是正常。<br />
<br />
　　本来一个具体的问题，用事实加逻辑就可以讲清楚了，结果常常讨论到后来，<br />
一方就跳出来说“我是弗里德曼的信徒，弗里德曼的东西，你都看过么？”那个<br />
回击“我是凯因斯的信徒，凯因斯的东西你都读懂了么？”或者一方说“我是追<br />
随哈耶克的，哈耶克的书你都看过么？”那个回击“我是研究施密特的，你把施<br />
密特的东西看完了再开口说话吧。” 讲到这里讨论已经没法进行了，双方都认<br />
为自己占据了理论的制高点，对方已经输了。<br />
<br />
　　5，<br />
<br />
　　伯恩施坦、波普尔、哈耶克、施密特那些名字在这帮知识分子嘴里，让我想<br />
起先民的图腾。图腾这个概念来自印第安语，意为“属彼亲族”。原始人相信每<br />
个氏族都与某种动物植物或其他自然物有亲属或其他特殊关系，一般以动物居多，<br />
比如熊、狼、蛇之类，即是该氏族的神圣标志，可以保佑这个氏族部落的强大。<br />
全世界所有的文明在其起源之初都能找到一些图腾崇拜的印记，比如中国的龙形<br />
象，据专家考证也来自不同部落的图腾。这种传统有非常强的生命力，我小时候<br />
在农村，长辈告诉我，在老宅子下面，或者冬天的灶房里发现了蛇，是非常吉利<br />
的，不能打扰它。我当时不能理解，后来读书多了，知道根据考古发现，在现在<br />
的苏南尤其宜兴一代的的先民以蛇为图腾崇拜。<br />
<br />
　　先民会把自己族群的兴盛和其所崇拜图腾的兴盛联系在一起。且举行崇拜仪<br />
式，以促其“蕃衍”，有很隆重的献祭，甚至要拿地方部落的活人，甚至像印地<br />
安一些部落，割了敌人的头皮来献祭，这样自己的图腾就会更加保佑自己，就可<br />
以使自己的部落威力大增。<br />
<br />
　　那些讲什么问题，不会自己用事实和逻辑来说话，要把施密特、波普尔、弗<br />
里德曼高高举起的当代知识分子，虽然穿着西装皮鞋，但在我看来何尝不像茹毛<br />
饮血的原始人呢？就像原始人会把一些图腾的羽毛或者爪牙装饰在自己头上和胸<br />
前，这样就可以借到图腾的神力。那些知识分子把施密特、波普尔、弗里德曼高<br />
高举起的时候，一定有一种陶醉感，就像原始人围着火堆跳舞，把自己的图腾高<br />
高举起时一样。他们的逻辑是一样的，我们的图腾是最牛的！这样我们的部落就<br />
是最牛的！！这样我就是最牛的！！！<br />
<br />
　　哈哈，看谁敢来惹我。<br />
<br />
　　更有汪晖这样的学者擅长引用各种名人名言,但你却看不出他自己的论据和<br />
逻辑关系是什么?甚至写了几本书,你都读不出他核心观点到底是什么?就像一只<br />
母鸡,把很多孔雀羽毛插在自己屁股上。而这一招在中国确实能把很多人吓一跳。<br />
<br />
　　6，<br />
<br />
　　五四时期的中国知识分子们也是这样的，理论界也是城头变幻大王旗。不过<br />
那时候还没有施密特、哈耶克这些名字，那时候的时髦是，谁说自己是马克思的<br />
信徒，是卢梭的信徒，或者杜威的追随者，把这些人的名字挂在嘴上，也是在人<br />
前觉得自己牛得不得了。<br />
<br />
　　时代过得很快，我们的物质世界每天都在发生变化，时间又过得很慢，我们<br />
的人性，最本质的密码其实几千年来都没有怎么变。即使觉得自己最理性的知识<br />
分子，如果不能以极大的毅力来自省，他们所表现出来的习性和原始人并不会有<br />
很大的区别。<br />
<br />
　　五四之后，还是年轻学生殷海光问他的老师金岳霖，现在有这么多主义，都<br />
很热闹，每个主义都跟着一大帮人，哪个主义比较好？金岳霖说，那些热热闹闹，<br />
特别时髦的主义，都不见得能够持久。真正经过你自己的理性检验觉得是对的东<br />
西，才真正有价值。<br />
<br />
　　不理解这一点，即使我们今天可以很容易地被很多现代理论所武装，我们的<br />
心灵习性并不能告别蒙昧和蛮荒。</div>

							
							
							
							
