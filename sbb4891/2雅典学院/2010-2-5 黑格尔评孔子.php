													<h2>黑格尔的邪恶之处</h2>
						
						
																											<div id="postmessage_468734" class="t_msgfont">第一种，就是所谓的辩证法。辩证法纯属胡说八道，这年头没有任何正经人会信。但问题并不在于它胡说八道（胡说八道的东西多了），而在于它可以解释一切：为所有邪恶的行径（无论是保守的还是革命的）辩护。<br />
黑格尔有一句名言：存在的就是合乎理性的（意思就是说：存在的东西，总得有点什么说得通的原因吧？）。经常被偷换成：存在的就是合理的。被偷换当然不是黑格尔的错，但第一个偷换这个名言的人却是黑格尔本人。黑格尔就是用这类理论为德国当年的反动制度辩护的。<br />
第二种，就是大言不惭。这个跟辩证法有关，但不完全等同，被卡尔波普尔深恶痛绝。波普尔认为：知识分子要以谦逊和能让人听懂的方式来向世人介绍自己的研究，而不能故弄玄虚。<br />
叔本华评价道：为此，我要说这位黑格尔的所谓哲学是一种故弄玄虚的神秘主义，将是后人嘲笑我们这个时代的取之不尽的话题，是一种麻痹所有精神力量，扼杀任何实际的思想，和借助对语言最可耻的滥用，用最空洞、最无意义的、最无思想的以及如同结果所表明的，使人蠢不可及的废话来取代这种实际思想的伪哲学，这种伪哲学以无中生有、荒谬可笑的突发奇想为核心，既没有根据又没有结果，也就是说，是什么也不能加以证明和本身也证明不了什么或说明不了什么，它还缺乏独创性，只是对经院哲学的现实主义以及斯宾诺莎主义的一种单纯的模仿，倒过来看，又是基督教想要提供的一种怪物......（叔本华反黑格尔的言论，经常被波普尔引用）<br />
第三种，就是极端国家主义。这个看起来似乎没什么，我前面已经说了”黑格尔为反动势力辩护“，这似乎就是国家主义的体现。反动势力虽然不好，也未必是大恶。但实际上，黑格尔的国家主义后果非常严重。因为在当时的背景下，国家和民族的概念往往是分不开的（都是nation，即纳粹的纳）。黑格尔实际上是德意志极端民族主义的一个重要源头。纳粹主义主要推崇的三位哲学家，分别是：黑格尔、叔本华和尼采。叔本华是反民族主义者，你可以理解成他专门跟黑格尔作对，实际上也因为他受佛教影响。尼采是贵族主义者，欧洲的贵族有国际性，缺乏民族性，虽然这跟尼采理想中的贵族有区别，但尼采也绝不是民族主义者。那么纳粹的极端民族主义是从哪儿来的呢？就是黑格尔。<br />
