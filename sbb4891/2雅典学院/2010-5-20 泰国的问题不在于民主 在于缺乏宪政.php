&lt;&lt;&lt;&lt;&lt;&lt;<br />
&nbsp; &nbsp; 尽管泰国的现代民主化进程早在1932年君主立宪制政体确立后就已开始，但随即就因为军人专政而中断。直到20世纪70年代军人独裁政府崩溃后，民主化进程才恢复发展，但由于仍受到军人势力的阻碍而进展缓慢。90年代初军人势力彻底退出泰国政坛后，泰国的民主化进程开始进入全面发展阶段。<br />
&nbsp; &nbsp; 在过去的70多年里，泰国先后颁布了16部宪法。虽然泰国的军人独裁政府在通过政变夺取政权后，多数会颁布新宪法以标榜自身政权的合理与正当性，但这些近似于&quot;钦定&quot;的宪法大多徒具民主的外衣，而根本无法体现民主的本质，有的甚至连民主的掩饰也彻底抛弃。如1958年政变上台的鼓吹&quot;泰式民主&quot;理念的沙立・他纳叻元帅，就是通过1959年宪法第17条使总理获得了凌驾于法律之上的绝对权力，从而为军人独裁专政铺平了道路。<br />
&nbsp; &nbsp; 笔者认为，泰国真正的宪政开始于1992年民主宪法颁布时，不过由于这部宪法还处于民主的草创期，因此许多内容并不完善。而其中最大的缺陷就是民主监督体制和选举制度的不完善，造成了泰国官场腐败成风，贿选随处可见，政坛小党林立、党派纷争不断，政客为谋求利益频繁&quot;跳槽&quot;，结果造成泰国政局混乱，直接侵害了民众的切身利益。可以说，1997年亚洲金融危机首先在泰国爆发与此不无关系。<br />
&nbsp; &nbsp; 不过，泰国却以此次危机为契机，制定并颁布了1997年宪法，对1992年宪法中的不足进行了修正与完善。不少学者和政治观察家认为这是泰国民主化进程的又一里程碑，预示着泰国将步入宪政的新阶段。2000年，泰国根据1997年宪法成功举行了有史以来的首次参议院直选。为了彻底杜绝贿选等舞弊现象，不少选区在新设立的独立机构--选举委员会的要求下进行了补选，个别席位甚至经过5轮投票才最后选定[01]。2001年，泰国举行了1997年宪法颁布后的首次众议院大选。他信・西那瓦领导的泰爱泰党在大选中全面胜出，并通过先后吞并自由正义党和新希望党而成为泰国自70年代以来第一个获得众议院简单多数席位的政党，组建了自90年代以来最为稳固的政府，从而在形式上结束了长期以来的混乱政局。<br />
