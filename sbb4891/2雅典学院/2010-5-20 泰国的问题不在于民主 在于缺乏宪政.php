																											<div id="postmessage_628042" class="t_msgfont">写实老哥这个观点&nbsp;&nbsp;有点闭门造车了<br />
<br />
刘松萝这句话我觉得挺好：- 民主必须在制度，而不是潜规则下面运行。军人和国王的特权，已经对民主乃至国家的运行有害了。<br />
<br />
还有这个帖子的7楼&nbsp;&nbsp;建议看看：<a href="https://1984bbs.com/viewthread.php?tid=46470&amp;extra=page%3D1" target="_blank">https://1984bbs.com/viewthread.php?tid=46470&amp;extra=page%3D1</a><br />
<br />
我把这个7楼贴这儿吧&nbsp;&nbsp;顺便先表态：力量才是一切的根基&nbsp;&nbsp;想靠宪法几张纸就如何如何那太搞笑了&nbsp;&nbsp;最起码得有某种利于宪政民主的主流文化&nbsp;&nbsp;才能保护宪法不变废纸&nbsp;&nbsp;而这种主流文化&nbsp;&nbsp;其实就是一种赤裸裸的力量：大部分人明确支持的，才能是主流文化，而“天下之大不韪”就是一种令人不敢轻“冒”的力量，会影响到政治家对风险与收益的计算<br />
<br />
<br />
<br />
<br />
<br />
文章提交者：孙吴 加帖在 猫眼看人 【凯迪网络】 <a href="http://www.kdnet.net" target="_blank">http://www.kdnet.net</a><br />
<br />
首先要感谢刘先生的好文章，深入的剖析了目前泰国民主存在的问题，对泰国民主的发展提出了一些很重要的看法和建议。我个人感觉这是个非常重要的问题，建议版主顶置，引来广大网友的深入探讨，进一步明了民主发展过程中的各种问题和困难。<br />
<br />
但是，我认为文章没有抓住目前泰国民主进程的要害，因而总结的经验教训既不能指明泰国民主的发展道路，也不能给其他民主发展中国家提供可取的经验。<br />
<br />
一、政治是关于权力平衡的艺术。<br />
<br />
泰式民主的最大特点和最大的弊端就是：王室、军队、民选政府三者之间的平衡。从1932年开始，这三者之间关系决定了泰国政治的走向。<br />
<br />
第一股势力首先是军队。军队在泰国始终是一股独立的力量，也是最强的一股政治力量，这就是1932年以来泰国不断政变的根源。历次的民主运动只是削弱了军队的影响和势力，但是并没有动摇它独立的地位，可以说1991年以来的历次民主运动都没有下力气去解决这个问题。<br />
<br />
第二股势力是王室。百足之虫，死而不僵，实施立宪制度后，王室并不甘心推出政治舞台，而是采取了极为巧妙的策略，置身于实际的政治操作之外，在军队和民选政治之间玩弄平衡。结果是既使自己不用承担政治失败的责任，享受巨大的崇高的民众拥戴，又在实际的政治运作掌握了巨大的政治权力。只举一例：现在还有所谓的“诋毁国王罪”，各政党竟然还恬不知耻的利用这个罪名打击对手，当然得利者只能是王室。<br />
<br />
放眼当今世界政坛、综观中外历史，王室、军队、民选政府这样奇特的权力平衡时是根本不可能实现的，这就是目前泰式民主最根本的缺陷。不是王室压制军队、政府，就是军队压倒政府，所以泰国就是不停的政变、不断的政治动荡。正常的民主对抗也会被军队和王室利用，以达到他们自己的目的。反过来说，政局不动荡了、平稳了，军队、王室反而会被逐步削弱影响和力量，目前这种混而不乱的局面反而是他们孜孜以求、苦心经营的结果。<br />
<br />
到目前为止的民主政治的实际经验总结出来的教训是：在政治操作上，国会、政府、最高法院三者之间的权力平衡；在政治力量上，资产阶级、中产阶级、贫民之间的权力平衡；是可以长期保持的。手段就是普选和稳定的宪法。<br />
<br />
总结一下：到目前为止，泰式民主的发展走到了尽头，之前的改革全然走错了方向。我认为适当的方向是：逐步取消军队的独立性，逐步消除王室对政治的巨大影响，提升民众的政治实力，增强民众对政治实际控制能力，让选票成为决定性的政治手段。<br />
<br />
二、民主是靠力量奋斗得到的，不是靠纸上的宪法得到的。<br />
<br />
目前很多人对正确的政治制度（尤其是宪法）抱有过高的期望，现在看到泰国民主的实际发展，应该清醒过来了。<br />
<br />
民选政府上台了多少次，泰国宪法就制定了多少回，尤其在他信政府时期的新宪法，吸取了泰国民主发展历程上的诸多血泪教训，泰国大多数政治家都以为应该是比较完善的了。结果不过几年，又变成了一纸空文。<br />
<br />
成功的宪法这些政治制度，不是几个政治家在书斋里、密室内制定出来的，它是民众艰苦奋斗的民主权利的真实反映，如果它是一部给民众恩赐权利的诏书，如果它是政治家脱离现实、自以为是的理论，在它垮台的时候，民众也自然漠然视之。他信政府被非法政变推翻时，整个泰国异常的平静，民众该干嘛干嘛，这就足以证明，泰国宪法与泰国民众的关系是多么的疏远，这个宪法又是多么的滑稽可笑。<br />
<br />
用血挣来的权利，民众自然会用血去捍卫，反民主的阴谋家才会投鼠忌器。<br />
<br />
三、政治革命必须要有文化革新的配合。<br />
<br />
这次政治运动最可笑的就是：人民联盟提出70%的议员要通过委任的方式“选出”，这足见一部分泰国民众对政治的认识到了多么可笑的程度。其实这还不是最奇怪的，“诋毁国王罪”这种中世纪烂货居然还在泰国堂而皇之的大行其道。民主政治在这样的环境内能有多大的进展，实在让人怀疑。<br />
<br />
这不是泰国一个国家的问题，目前整个中亚都面临这个难题，民主和这些国家国内落后的文化格格不入，要么因此民主进程困难重重，要么民主反而结出独裁、恐怖主义的毒瘤，让楼主刘先生很在意的民粹主义不过就是泰国落后文化的反映罢了。没有一场较为彻底的“脱亚入欧”、“五四”运动，没有对本国文化进行深刻的反思和检讨，民主的进程就不会顺利，民主就会走歧路。说到底，有什么样的人民，就有什么样的民主。这谁也不能埋怨，只能自己埋怨自己。<br />
<br />
以上均为我个人的观点，欢迎各位批平指正。</div>

							
							
							
							
