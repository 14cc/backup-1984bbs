追求民主的人都知道,专制权力的滥用是万恶之源,所以他们希望权力这个东西不应该由某个人或某个政党垄断,而应该由人民来掌握.但是,我们必须要明白,权力这个东西无论掌握在一个人手里,少数人手里亦或是多数人手里,都有被滥用的可能.这就是美国&lt;权利法案&gt;第一条直指国会,限制国会的原因.<br />
结合泰国的例子,军队居然都能跳出来影响政治,那只能说明军队国家化还远远没有实现.在成熟的宪政民主国家,无论政治风云如何变化,政党之间如何死掐,政客之间如何拍砖,军队都应该保持中立和自制,军队跳出来了,不出乱子才怪.反过来讲,如果军队能够服从宪法,捍卫宪法,保持中立和自制,那么社会再怎么乱,也不会大乱.</div>

							
							
							
							
