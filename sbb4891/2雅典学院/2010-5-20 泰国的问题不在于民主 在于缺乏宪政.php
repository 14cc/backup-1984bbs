&nbsp;&nbsp;在成熟的宪政体制国家，民选政府的权力同样需要受到来自法院和议会的制衡和监督，而这些在亚洲新兴民主国家则容易缺乏，往往导致了民选政府的权力过大。<br />
&nbsp;&nbsp;泰国从1932年以来发生20多次政变，通过了18部宪法，92年才出现第一个民选文官政府，但又被军人推翻。<br />
&nbsp;&nbsp;只有全民民主选举总理是不够的，议会、法院对于政府的制约太弱，法院的独立性和权威性都不够，虽然开放了全民选举，但是各方都谋求“胜者通吃”而非权力制衡的状态，此种失衡的权力构架，也容易导致最糟糕的军人干政，最终各方都希望依靠军人支持取得独大的权力，导致一次次超越宪政框架下的军事政变、民间起义式夺权。<br />
<br />
&nbsp;&nbsp;综上所述，泰国的乱象并非是由民主制度导致的，而是出在缺乏宪政法治的权力制衡，无论对民选政府的制衡、限制，还是对军人权力干政的制衡，以及对民间各势力示威暴力化的法律控制，都缺乏权力在宪政框架下的制约机制，尽管危机重重，但泰国毕竟已经走出了关键性的民主步伐，现在急需的是完善起能够配合民主制度的宪政制衡框架，这才是挽救泰国民主成果的关键。</div>

							
							
							
							
