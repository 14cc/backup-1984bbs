													<h2>邪教的定义，其实这么看轮子还真不能算是邪教</h2>
						
						
																											<div id="postmessage_550551" class="t_msgfont">[编辑本段]邪教26个特征<br />
<br />
　　邪教所提供的，正是你很长时间以来寻找却没有找到的。很奇怪，他正好知道你缺什么。 <br />
　　与邪教的第一次接触就为你看问题打开了全新的视线。 <br />
　　邪教的世界观非常简洁明了，能够藉以解答一切问题。 <br />
　　邪教试图从情感上争取你，其他成员很快就成了你的“好朋友”。 <br />
　　你觉得提出批评性看法，那些新朋友会感到失望，而且，如果你总得拒绝新朋友不断的邀请会觉得过意不去，因为你不愿意做一个不讲礼貌的人。 <br />
　　邪教越来越多地进入你的生活，逐渐形成一种引力：似乎进去容易出来难。 <br />
　　尽管如此，你还是不清楚邪教的情况。对你的要求是，不要思考，不要检验。 <br />
　　你的新朋友会说：“这没法解释，必须自己去体验，我们所经历的完全是另外一个层次……”或类似的说法。 <br />
　　邪教有一个拥有真理的大师或者是神的传话人，他的决定权不受任何怀疑。 <br />
　　邪教的教义被视为是唯一真确、永久正确的知识，拒绝传统的科学，拒绝理性思维，拒绝理智，认为这些只重视脑，是负面的，是属于魔鬼的等等。 <br />
　　邪教缺乏理性的讨论文化：因为邪教认为自己拥有不可改变的真理，所以认为一切关于自己的教义和组织的讨论都是多余的。 <br />
　　邪教内部的批评者被诬陷、隔绝或开除。 <br />
　　来自邪教外的批评被当作邪教正确的证据。 <br />
　　邪教总感觉自己被敌对势力包围，外部的人都在对自己施展阴谋。 <br />
　　邪教只认识两种人：要么是朋友，要么就是敌人，谁不是我们的支持者，谁就是我们的反对者。 <br />
　　世界面临着大灾难，只有邪教自己知道，如何让这世界或自己的信徒得救。 <br />
　　邪教内部的人是精英，外部的人都在走邪路，充其量是传教的争取对象。 <br />
　　邪教可以为了实现自己的目标不择手段。 <br />
　　邪教内部通过特别的标志或行为规则与其他人隔离开来，比如通过自己的语言、特定的服装、饮食、对日常生活的控制和对私人关系的干涉。 <br />
　　邪教促使你与到目前为止的社会关系隔离，原因是这些社会关系影响你的进步。 <br />
　　邪教促使你把自己的资讯来源局限于邪教自己的意识形态（不看新闻，不读报纸，不读文学作品，只读邪教内部的读物） <br />
　　邪教给你安排任务，要求你参加活动，占用你几乎所有的时间（比如卖书刊、参加学习班、参加活动、祈祷、打坐、共同读书、拉新人入教）。 <br />
　　你很少一个人单独活动，总是有邪教的人来接你，或许你还被要求到邪教的集体住处去住宿，对你的个人生活的控制被称为是对你坚持走唯一正路的支持。 <br />
　　邪教也为你做通常由每个人自己做的决定。 <br />
　　邪教公开或暗示性地要求你严格服从，甚至要求你放弃自我，因为这是取得进步或得救的唯一途径。 <br />
　　如果你对邪教产生怀疑，不会有人关心你怀疑的原因， <br />
　　更多的是，你将会听到某些现成的解释：这套体系本身没错，只是你还没到时候。 <br />
　　如果你没能取得进步，那么原因只在你自己，因为你信得不够，读得不够，理解得不够，祈祷不够，打坐不够或者是上课上得不够等等。 <br />
　　有时候你会觉得这个邪教可怕，觉得自己受到了压力，可是你很快就排除这种念头，并且尽力使自己更加靠近这个邪教。<br />
Steve Hassan(史蒂夫・哈桑)对邪教的定义<br />
<br />
　　Steve Hassan2000年出版的 &quot;Releasing the Bonds: Empowering People to Think for Themselves&quot;，第二章对邪教的解说：<br />
　　“邪教四个主要原素：当四项在整体上加强人对教主的服从和依赖，即有可能是邪教组织，不是要所有以下的各项目齐全方算得是的：”<br />
　　行为上的控制<br />
　　住处，家庭，同居者，往来者受到规范； <br />
　　穿着，打扮等受到规范； <br />
　　食物受到规范； <br />
　　睡眠受到规范； <br />
　　无消闲的空间。 <br />
　　教义的灌输及组织活动（如念经文等）占去人最大部份的时间； <br />
　　主要决定不能自作，必先取得批准； <br />
　　须要汇报思想，感受与行动予教长； <br />
　　存在行为责罚制度； <br />
　　团体思维高于个人； <br />
　　强硬的规条； <br />
　　须要依赖和服从。 <br />
　　信息控制<br />
　　使用谎言，包括不发放消息，歪曲信息； <br />
　　减低教外信息的散布或不鼓励接触这些；包括对教批评的说话，和使成员过度忙碌以至无暇接触外界； <br />
　　做成教“内”和教“外”对垒的局面，由教主决定谁人可知甚么； <br />
　　鼓励教徒互相举报; 成立“伙伴”制度（buddy system）； <br />
　　大量并广泛使用教义及由教派传播的信息，并大肆宣传； <br />
　　不道德的“告解”使用，以过犯作为控制手段，不随便宽恕。 <br />
　　思维控制<br />
　　将教义定为“真理”－黑白分明的思维；善对恶的思维；教内对垒教外的思维； <br />
　　使用特殊语言代替正常的思想表达； <br />
　　只容许“好”及“正确”的想法； <br />
　　压制思考－只许“好”的思想，否定批判、理性想法以及建设性的批评； <br />
　　空想的运用； <br />
　　唱出咒语； <br />
　　冥想； <br />
　　祷告； <br />
　　不能批评教主或教义； <br />
　　不容纳别的信仰。 <br />
　　情绪控制<br />
　　使人的感受受控制； <br />
　　相信所有问题都是自己的错而永不是领导的错； <br />
　　过度夸大罪恶感； <br />
　　过度夸大恐惧－ <br />
　　怕自主； <br />
　　怕“外”界； <br />
　　怕敌人； <br />
　　怕失去救赎； <br />
　　怕离开教派； <br />
　　怕遭否定。 <br />
　　使用过大的情绪波动来控制人； <br />
　　公开悔罪； <br />
　　非理性的恐惧－害怕领导人，害怕离开，不会反驳领导，如离开即对前途无信心； <br />
　　教外没有快乐可言，故不能离去； <br />
　　离开即有祸； <br />
　　对离去者杯葛； <br />
　　离去即属堕落。</div>

							
							
							
							
