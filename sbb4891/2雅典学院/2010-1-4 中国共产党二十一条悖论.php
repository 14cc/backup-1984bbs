													<h2>“东方红”和“国际歌”：两套话语体系</h2>
						
						
																											<div id="postmessage_801587" class="t_msgfont">(转自凯迪) <br />
<br />
小的时候，家乡小县城广播站的早间节目，大约6点半在“东方红”的歌声中开始；晚间节目则在8点半在“国际歌”的歌声中结束。天天这么听，也不觉得有什么不对头。 <br />
<br />
突然有一天，可能是开窍了，会思考问题了，就觉得很纳闷：“东方红”中说道：“他是人民大救星，呼儿嗨哟，他是人民大救星！”然而，国际歌却言道：“从来没有救世主，也不靠神仙皇帝”。这不是互相矛盾吗？难道是我理解有问题，“大救星”和“救世主”不是一个意思？百思不得其解，想不通。去问大人，大人也说不清。 <br />
<br />
后来打倒“四人帮”，破除个人迷信后，这个问题才算得到解决。 <br />
<br />
但同时，另一问题却仍然很长时间没有得到解决。那就是为什么中国人会同时接受这两种截然不同的说法呢？他们不思考问题？或者他们没有逻辑思维能力？或者是早晨稀里糊涂，而晚上则头脑清醒？早上是臣民，而晚上就变成了共产主义战士？ <br />
<br />
对这一问题，我现在的解释是认为这是两套话语体系。“国际歌”是一套话语体系，而“东方红”则是另一套话语体系。“国际歌”开初的话语体系，用来诉求于社会精英；而 “东方红”是后来的话语体系，用来诉求于底层大众。 <br />
<br />
对于社会精英，特别是接受一些启蒙教育的社会精英，用“东方红”来打动他们，动员他们参加革命无疑是没有作用甚至有反作用的。而用“国际歌”就很有可能。 <br />
<br />
但对于劳苦大众，对于那些没有受到启蒙教育的底层大众，你用什么国际歌来打动他们，动员他们参加革命，也是没有多大效果的。因为他们仍然囿于传统文化，相信“真命天子”。所以，你说谁谁谁头顶上有五彩云、谁谁谁大难不死之类的“神话”，对他们更有吸引力。 <br />
<br />
因而，我们就会看到在某个阶段，某个利益集团成型了后，这个集团为了谋求更大的权力和利益，它就会抛弃原来的诉求而去迎合底层民众，而不是去改变底层民众的认识，因为这样做时日太长，任务太艰巨，利益集团还很有可能解体。所以，这个集团内部上下会一致地主张、赞同、支持或默许推举出一位领袖，沿用这个国度曾经一再使用惯常作法，以此来凝聚力量。这样才能生存，才能发展。纵然这种生存与发展已经偏离或背离了原有的方向。 <br />
<br />
对于那些底层民众，他们只听得懂“东方红”，或者只需要听得懂得“东方红”就行了。而对于那些精英，他们虽然两者都听得懂，但后来他们在其中做出了选择。为了集团和自身的利益，他们也选择了“东方红”，转而将“国际歌”看成一种符号而已。 <br />
<br />
然而，最诡异也最可叹的是，那些促成“黄袍加身”的人，后来却铁定避免不了悲惨的下场。</div>

							
							
							
							
