																											<div id="postmessage_684713" class="t_msgfont">网评员报酬:<br />
&nbsp; &nbsp;&nbsp;&nbsp;实习期月薪1500 <br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;正式上岗月薪3000<br />
&nbsp; &nbsp;&nbsp;&nbsp;主要论坛网评员小组长月薪5000<br />
&nbsp; &nbsp;&nbsp;&nbsp;突发事件加班评论发双倍工资<br />
&nbsp; &nbsp;&nbsp;&nbsp;专职必须大学专科以上学历。<br />
&nbsp; &nbsp;&nbsp;&nbsp;兼职网评员，按发帖质量数量计酬。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp;&nbsp;&nbsp;总则：<br />
&nbsp; &nbsp; 网络舆论战争关系到国家政权的生死存亡。为了祖国母亲的繁荣富强，为了中华民族的复兴，每一个网络评论员必须时刻准备着用自己的智慧和艰苦劳动保卫共和国的网络防线。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp;&nbsp;&nbsp;基本工作方法：<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp;&nbsp;&nbsp;1、在工作时间内必须每小时至少查看一次工作邮箱，时刻注意领会 上 级 指 示的最新精神。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp;&nbsp;&nbsp;2、网络 评论员 根据 上 级 指 示 进行合作，根据工作需要，将由跨地区、跨专业的网 络 评 论 员 组成工作小组，执行特定的任务。在有必要增加人员时，上级将从其他小组抽调人员加以充实。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp;&nbsp;&nbsp;3、基本工作方法：日常工作按照网站分小组，每个重要网站的有关论坛由一个小组负责。日常工作是按照总体方针，维护正确的网络 舆论 导向。遇到 突 发 事 件，则按照上级部门的专门工作组的指令行事，暂时停止日常工作，把有关人员资源投入到突 发 事 件的舆论导向工作。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp;&nbsp;&nbsp;4、网络 评 论 员 要善于隐瞒自己的真实身份，必需有多个不同的网名，而且不同的网名要发表不同风格的文章。必要的时候，可以由不同小组成员制造网友辩论的假象，然后由第三方推出强有力的证据，把公众舆论引导到第三方。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp;&nbsp;&nbsp;5、某些网络 谣 言 出来的时候，必须尽快搜索到 谣 言 的首发地点和首发人，然后勒令网站管理员删除原贴，网络 评论员 则拷贝内容，以不同的IP地址发表自己就是事发所在地的当地人的申明，然后由版主或以其他网友身份指出：他的IP地址不在事发所在地，该消息纯属谣传。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp;&nbsp;&nbsp;6、必要时可以制造更加耸人听闻的假 新闻，吸引网民视线，然后很快澄清该消息纯属谣言。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp;&nbsp;&nbsp;7、某些论坛人气不错，网友信用度比较高，这时首先要做的是制造一种混乱，通过似是而非的文章进行干涉，跟贴作非理性的故意曲解、制造误会和争辩，转移网民注意力。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp;&nbsp;&nbsp;9、不断学习，提高文字水平，学会使用不同的文笔风格写作，善于模仿他人文笔，这是网络 评论员的基本功。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp;&nbsp;&nbsp;10、学会与网友交流的技巧，与网友私下打成一片，获取网友的信任，尤其是那些文章有影响力的网友。如果有可能，争取一些重要论坛的版主位置。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp;&nbsp;&nbsp;11、培养高超的判断力，能够在诸多贴子中迅速找到真正有影响力的帖子和写手，作为重点工作对象。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp;&nbsp;&nbsp;12、注意培养 政 策 法 规 意识，不可误解当前的工作精神。注意吃透上级 指示的近期发贴类型实例，融会贯通，举一反三。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp;&nbsp;&nbsp;13、灵活性与原则性相结合。一定要制造真假难辨的形象，成为一个不容易被鉴别身份的人。不仅要熟悉我们的观点，更要熟悉对方的思路，知己知彼。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
