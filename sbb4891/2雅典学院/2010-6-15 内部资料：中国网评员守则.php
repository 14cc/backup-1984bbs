&nbsp; &nbsp;&nbsp;&nbsp;总则：<br />
&nbsp; &nbsp; 网络舆论战争关系到国家政权的生死存亡。为了祖国母亲的繁荣富强，为了中华民族的复兴，每一个网络评论员必须时刻准备着用自己的智慧和艰苦劳动保卫共和国的网络防线。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp;&nbsp;&nbsp;基本工作方法：<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp;&nbsp;&nbsp;1、在工作时间内必须每小时至少查看一次工作邮箱，时刻注意领会 上 级 指 示的最新精神。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp;&nbsp;&nbsp;2、网络 评论员 根据 上 级 指 示 进行合作，根据工作需要，将由跨地区、跨专业的网 络 评 论 员 组成工作小组，执行特定的任务。在有必要增加人员时，上级将从其他小组抽调人员加以充实。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp;&nbsp;&nbsp;3、基本工作方法：日常工作按照网站分小组，每个重要网站的有关论坛由一个小组负责。日常工作是按照总体方针，维护正确的网络 舆论 导向。遇到 突 发 事 件，则按照上级部门的专门工作组的指令行事，暂时停止日常工作，把有关人员资源投入到突 发 事 件的舆论导向工作。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp;&nbsp;&nbsp;4、网络 评 论 员 要善于隐瞒自己的真实身份，必需有多个不同的网名，而且不同的网名要发表不同风格的文章。必要的时候，可以由不同小组成员制造网友辩论的假象，然后由第三方推出强有力的证据，把公众舆论引导到第三方。<br />
