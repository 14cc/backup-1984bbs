													<h2>刘晓波“我没有敌人”正确吗？</h2>
						
						
																											<div id="postmessage_475780" class="t_msgfont">刘晓波“我没有敌人”感动了许许多多追求自由民主的青年，也惊起一片喝彩，我却认为这句话不怎么样。我不知道刘晓波写出这行字时的真实想法，但无非三种可能：1、一种说法而已，表现宽广胸襟，其实他没这么想；2、他秉持这种理念，认为能化解仇恨，促进政治和解；3、他当真认为你没有敌人。<br />
<br />
我认为你把没有敌人作为宣传可以，作为秉持的理念也可以，可就是别把它当成真事儿，当真就是傻逼了。因为有没有敌人不只是你自己说了算，你即使认为你没有敌人，谁也不是你的敌人，依然会有人侵犯你，损害你，与你为敌，全然不顾你眼里是否有敌人。试想一位妙龄少女，天真浪漫的年龄，满眼的玫瑰色，认为人生美好遇到的每个人都心存善念，这时来一歹徒把她强奸了，你说她有没有敌人？<br />
<br />
敌人是客观存在的，你宣传你没有敌人你就是马基雅维利主义者，但是否有效则另说，也许会弄巧成拙；你秉持没有敌人的理念你就是政治不成熟，因为是否化敌为友是利益关系出现变化，或者对方理念出现变化，你的善意举动如果没引起上述两方面发生变化，敌对关系怎么可能消除？<br />
<br />
这时有人会说：“他的意思是没有私敌”，这个看法大概来源于恩格斯在马克思墓前的悼词，可是马克思可以这么说，刘晓波却不能这么说，这么说就是伪善，就是迂阔。原因在哪？原因在于马克思虽然论敌无数，屡遭驱逐，可毕竟被作为人来对待。可当恶警对你恶言恶语时，对郭飞雄生殖器动刑时，在学校逼迫格格时，你能说他不是你的私敌？当他那张肮脏的手朝你打过来的时候，当他对你恶语谩骂的时候，他就不仅代表政权，他呈现的是一个完整的人格，他还代表他自己，他就是你的敌人。<br />
<br />
<br />
施米特说过政治的本质就是分清敌友，你当真认为没有敌人，这政治游戏你还怎么玩下去那？</div>

							
							
							
							
