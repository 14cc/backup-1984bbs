													<h2>任彦芳：我见到的艾未未</h2>
						
						
																											<div id="postmessage_305200" class="t_msgfont"><a href="http://www.21ccom.net/newsinfo.asp?id=3438&amp;cid=10352200" target="_blank">http://www.21ccom.net/newsinfo.asp?id=3438&amp;cid=10352200</a><br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 为什么我的眼里常含泪水，&nbsp;&nbsp;<br />
　　是因为我对这土地爱得深沉。&nbsp;&nbsp;<br />
<br />
　　&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; ――艾青&nbsp;&nbsp;<br />
<br />
　　我知道艾未未，是在网上。他是北京奥运会主会场鸟巢设计者中方顾问，鸟巢是他的概念；没有艾未未就没有现在的举世瞩目的鸟巢；我看到了他自己写的博客，是在牛博网上。我从这儿看到他的一颗为国为民的赤子之心；可后来他的博客被封了，再也听不到他直接写出的声音。&nbsp;&nbsp;<br />
<br />
<br />
<br />
<br />
<br />
　　我了解艾未未，是从他的母亲高瑛那里。我几次去高瑛的家里，都是硬笔书法大师庞中华开车去，每次都要听高瑛说起她的儿子未未。母亲说起儿子，为儿子骄傲，也为儿子担心。她说，我总劝未未要小心谨慎，可他不听我的话，我没有办法；我跟未未说，你要记取教训，少说让官方不爱听的话，不要惹事，可儿子说，我的身上难道没有你和爸爸的基因吗？&nbsp;&nbsp;<br />
<br />
　　于是，高瑛对我们说起了未未。他出生在1957年6月，那时候，艾青被打成了右派，儿子出生要报户口，可没有名字，艾青哪有心思给儿子想名字？他对我说：你把辞海拿过来吧。他打开辞海，闭着眼睛，用用一按，睁开眼一看是个“威”字；威权，威力，威望，威风，艾青说，有什么好威风的呀，这个字不好。他便读出这个字的四声，未，未，让儿子相信未来，就叫他未未吧。&nbsp;&nbsp;<br />
<br />
　　高瑛讲说了儿子和他们一同经受的苦难，正是这苦难大学，把他培养成一个不忘记百姓，总站在受难者的立场上，为百姓说话吧。&nbsp;&nbsp;<br />
<br />
　　在高瑛大姐的会客室里，一角是我最喜欢的诗人艾青的铜像。那天，我们合影，我说就在艾老身边吧，好像他仍在，我闻到了他的生命的气息。这就是我面前的一幅高瑛和她儿女的大照片，那个站在母亲身边有一脸大胡子的，就是未未，我还想像不出他的性格。但从他身上我看到了诗人艾青的生命；在这张照片一旁，便是胡锦涛和高瑛谈话的照片，那是一九九六年五月五日艾青逝世之后的第二天，她没有想到，也没有通知，那天胡锦涛在作家协会党组书记，中宣部副部长的陪同下来到了我们说话的这间会客室，胡锦涛对艾青的逝世表示悼念，并请家属节哀保重身体。&nbsp;&nbsp;<br />
<br />
　　转眼十三年过去了。然而今天的高瑛去失去了平安和平静的日子。她的忧虑担心，常常使她睡不着觉，她无法保重自己。&nbsp;&nbsp;<br />
<br />
　　她对我说，有天突然来了不少公安警察，来了一院子人，说是要找我儿子艾未未。一个个对我怒目而视，好像我就是他们的敌人似的。我说，你找未未，你到他的住处找，他不在我这儿住。警察问：他的户口不是在这儿吗？我说，户口是在这儿，可他有自己的工作，他不在家里住呀。你们找他有什么事，不能和我说吗？他们不说，一定要找到未未，我是配合公安的，我说给了他们艾未未的地址，让他们去找，不然我给儿子电话，让他过来和你们谈话。&nbsp;&nbsp;<br />
<br />
　　我的儿子出了什么事呀，从警察的对我的态度，就可以知道他一定出了大事，这能不让母亲担忧吗？&nbsp;&nbsp;<br />
<br />
　　我给儿子电话，儿子说妈妈你不要为我担心，我不会作对不住人民的事情的，不要害怕。&nbsp;&nbsp;<br />
<br />
　　我这才知道，他在为地震死去的孩子做调查。他组织了一百多名志愿者，到了四川地震灾区，去一个一个地调查死去的孩子的家长，孩子多大，哪年出生，在哪个学校上几年级，他们听家长的述说，录了音，录了相。原来他一直做这个事情。他说，这是替政府做事，温家宝总理多次承诺的一定要调查清楚，这些孩子是如何死的，对多少学校的倒坍的建筑的豆腐渣工程，自然会引起官员的不高兴。可这是政府应当做出向民众交代的事情啊。&nbsp;&nbsp;<br />
<br />
　　我觉得儿子做的是正义的事业，我没有理由阻拦他。&nbsp;&nbsp;<br />
<br />
　　听了高瑛大姐的述说，我更想见见这位为民众维权的国际著名的艺术家未未。&nbsp;&nbsp;<br />
<br />
　　九月十二日，庞中华开车到了高大姐家。进门我们便问，艾未未回来了吗？&nbsp;&nbsp;<br />
<br />
　　因为我们从网上看到了消息，说他一个月前的八月十二日，为了接受被起诉为“颠覆国家政权罪”的作家谭作人开庭传唤证人，他十一日到了成都，没有想到的是，他到了宾馆警方非法拘禁不能行动，并且遭到了公安人员的殴打，艾未未受了伤害。结果是所有要为谭作人作证的人均不能出庭。听说，未未回到北京了，我们便想见他。上午，中华没有时间，便说晚上去吧。可我们到了高大姐家，她说：她的儿子刚走，现在还在飞机上，他要去德国举办他的一个展览，他带走了十八个大箱子的展品哪。&nbsp;&nbsp;<br />
<br />
　　真不巧，我们晚了几个小时，没有见到这个大胡子的未未。我问高大姐，未未被打了，他现在身体如何？&nbsp;&nbsp;<br />
<br />
　　高瑛说：未未没有跟我说他挨打的事，怕没有事吧。他说的是他在德国办展览，没有说别的，他急急忙忙地上飞机了。&nbsp;&nbsp;<br />
<br />
　　我们便说了从网上看到的一些情况。高瑛说：这孩子呀，他可能怕我惦记他，这些事他都不跟我说呀。不过，我总有种不好的感觉，总是心神不安。&nbsp;&nbsp;<br />
<br />
　　让我们祝福他一路平安，一帆风顺吧。&nbsp;&nbsp;<br />
<br />
　　九月十五日，我从网上看到了这样一条消息，大惊。&nbsp;&nbsp;<br />
<br />
　　疑被公安打至脑出血，艾未未在德国做手术&nbsp;&nbsp;<br />
<br />
　　明报：中国艺术家及维权人士艾未未14日在德国慕尼黑验出“重挫造成的外颅与脑体间大面积出血”，现已入院，拟于当地时间14日下午或15日上午做手术。本报14日晚与艾未未联络时，他说话声音较弱，与上月与四川公安交涉时大不相同。他说，医生表示瘀血压迫到大脑，令他说话受影响。艾未未上月12日到成都为维权人士谭作人案作证，凌晨时分被冲进所住酒店的公安在右脸打了一拳，当地医生验伤后鉴定为“轻度挫伤”。但艾未未说，他自那时起一直头痛，原以为过几日会转好，但14日到慕尼黑出席一个展览时，头痛难忍，故前往就诊。&nbsp;&nbsp;<br />
<br />
　　我要不要把这消息告诉高大姐呢，我怕她惦记儿子，还是不报吧。&nbsp;&nbsp;<br />
<br />
　　九月十六日早晨八点，我的电话响起来，是谁这么早来电话呢？我拿起电话，听到的是高大姐的声音，她的声音有些颤抖：&nbsp;&nbsp;<br />
<br />
　　“我昨天夜里接到从德国来的电话，未未因为被公安人员殴打的头部，住进了德国的医院，动手术了。医生说头部有淤血，医生说如果他晚来一天，怕就见不到他的展览了。。。我的儿子为什么会遭到这样的对待？他是为国家做事，他去作证，为什么要这样啊。我真不明白，他们是想做什么？我恨透了他们的流氓行径！我不怕有公安人员在监听我的电话，我就这样骂他们，我什么也不怕了。我儿子多亏这天到了德国，得到了抢救，如果是在中国，也许他就没有命了。有人会变着法子把我儿子害死的。他们这样背民心，害百姓，遭人民恨，他们还会长久吗？你也是正直的人，你总说真话，为百姓维权，你也要注意；他们也许会盯上你，这是你的光荣，可你要注意保护自己。因为我一宿没有睡，起来就想找人说说这一肚子心里话。”&nbsp;&nbsp;<br />
<br />
　　听着高大姐的话，听了这位母亲含泪的述说，我觉得她的心在流血；我也流泪了，我说不出话，只有一句，大姐你保重吧。&nbsp;&nbsp;<br />
<br />
　　国庆的第二天，下午三点，中华开车，我们去看望高大姐。她的心情比较平和了。她拿出了一个电视纪录片，名字叫《老妈蹄花》在会客厅里放给我们。这次她还请来了工人出版社的王先生与我们见面。这是真实纪录了未未被拘禁，被打，以及他们据理去找派出所，成都公安分局，成都公安局交涉的经过。从这里我看到了一个无所畏惧的艾未未，一个有理有力的向邪恶斗争的艾未未，一个真正的男子汉，一个不愧诗人艾青的儿子的艾未未！这部纪录电视片，用形象事实揭示了中国公安警察的现状，这是多么可怕的现实！我被这真实所震惊所震撼！&nbsp;&nbsp;<br />
<br />
　　看完电视片，高大姐说出她的想法，她想给胡锦涛写信，给温家宝写信，说说艾未未的情况，他是为了实践温家宝在地震时含泪做出的“一定要沏查公共建筑，给人民一个交代”的承诺而去做地震中受难的孩子的调查的呀。他是为了法庭调查，去为同样做孩子死难调查的谭作人案的证人去的呀，他犯了什么法，我儿子有什么罪呀，你们就这样对他下毒手呀！我想写信，让胡锦涛，温家宝知道。&nbsp;&nbsp;<br />
<br />
　　我们都支持她写这样一封信。我们说，你最好等你儿子回国后再和儿子商量，求得他的同意。&nbsp;&nbsp;<br />
<br />
　　10月25日，八点，高大姐便找了朋友的车来我家接我了。是她的儿子艾未未回来了。这真是难得的一见，我到了大姐家里，说起最近的情况，我向她讲了我正在搞的一部纪实文学作品，她提了意见。我说我没有精力，如果有的话，我想写艾青和他的儿子这两代人，写写你这位伟大的母亲。就是写写这次为地震孩子调查所遭遇的事，也是一篇震惊中外的报告文学呀。不知能不能找到这样的作家完成此题材的写作。过一会儿，王先生也来了，在家里吃了饺子，她便给儿子电话，说在家等我们。二点，我们的车便到了草场村未未工作的地方。&nbsp;&nbsp;<br />
<br />
　　走进了这个院落，如同走进了一个古代的城堡，四周的墙高大，院里的柿子正在成熟，如金黄色的灯；有两个高大的磁器立在院中，我们以为是两个灯；未未迎接我们，我一眼便看到他头部的开刀的疤痕，有两个洞，在剃去头发的地方。母亲一看，便心痛地说：儿子呀，你现在觉得怎么样啊，还痛不痛啊？未未说，妈妈你不用担心，没有事的。&nbsp;&nbsp;<br />
<br />
　　我们先在院内合影。艾未未，把他受伤的头部扭过去，照片将显示是没有伤口的一边；后来高瑛发现了这点，便特意要照一下儿子的头的右部，那被公安打伤而作手术的一边。儿子怕母亲惦记吧，他走进屋子里取来了他的头部艾克斯光照片，说：这个脑球如核头，这一边的血把这半部全站满了，把脑球挤歪了，这是生命垂危的病；妈妈你看出来了吧，现在好了，把这边的淤血全取走了，还会有一点血渗过来，没有关系的。&nbsp;&nbsp;<br />
<br />
　　儿子放回他的照片。我对高大姐说，我可以打开录音笔记录未未的谈话吗？大姐说，当然可以。于是我便真实地记录下未未的谈话了。&nbsp;&nbsp;<br />
<br />
　　未未说：我现在主要是注意力不集中。但会好的，三个月就会好起来的。我住院六天便出来了，办展览需要我。（我们正说话，发出了强烈的放炮声，未未说这要给他们谈谈，这样不行啊，我们说话都困难，我去给他们说一下。我们不让他去说没有关系的。未未说这样做是不行的。一会放一阵，没法说话。这在外国很简单。打一个电话他就不能这样。不然就上法庭了。）&nbsp;&nbsp;<br />
<br />
　　我向未未说起他妈妈想给中央领导人写信的想法。你看要写些什么呢？&nbsp;&nbsp;<br />
<br />
　　未未说，这有两个问题至少要说清楚。一是对事实真相的掩盖。你阻挡证人出证，在全世界都是不行的，除非黑社会才这样。做为执政党，你不能干扰公检法正当程序，这样还有公正可言吗，是谁怕这公正呢？这在法律上是绝对不许可的。&nbsp;&nbsp;<br />
<br />
　　二是执法犯法的事。你打了人还说没有打。你打我是小事，你这样做，全国要出多少冤假错案，多少民怨啊；你这样做会失去信任啊，这样你所有的政治理想全是扯淡。我真不理解他们为什么要这样做。国有国法家有家规，他们也都明白，没有必要说更多，说更多也没有用，胡温也是从基层上去的。现在已经烂透了，不好说了。新疆已经两月不能上网了。手机短讯也发不出去了。把新疆搞成成独立国了吗？你再说你的民族政策如何正确，你这两个多月的所作所为，就在全世界面前暴露无遗了。我不明白，是他们黔驴技穷了呢，还是不得不这样做呢？没有办法啦？我们说也是白搭，但说是我们的责任，是我们的义务&nbsp;&nbsp;<br />
<br />
　　王先生把话题转到未未的在德国办展览事。我问：你在墨尼黑举办的是艾未未艺术展览吗？他说：艺术展的名称是“非常遗憾”，德国的新闻媒体对我作了很多的报导，几百篇文章，整版或半版的，很多，大照片，长文章都有。法兰克福书展有个蓝沙发讲座，邀请我去，我没有去，一是因为我身体不大好，二是因为德国的报纸宣传太多了，我在媒体上说得很多了，我不想再去；说也还是要求出版言论自由哇。主持人在大会上说了，我不去是个遗憾。当时习进平在场。但有刚得诺贝尔奖金的罗马尼亚出生现为德国人的女作家米勒约我和她对谈。她在媒体上说明了这一点，她的作品是写劳改营的，她说，我活下来就是为了在劳改营里死去的人。她对极权政治表示了极大愤慨，她说对极权政治不能掉以轻心。她在谈话里讲到了我，网上可以看到，她得奖之前，就想和我对谈，我当时也不知道她会得奖。明年三月，我们对谈吧。&nbsp;&nbsp;<br />
<br />
　　说到这个月十八号林希翎的追思会的事情。他们约我写封信，我没有写，我脑子也不好，我刚回国，也没有去。我听说，一百多人去了，为这个会停了水电。说明他们很软弱。很虚。不光明正大，你拥有所有的资源，你怕什么，让人家开会得了，一个对人的思念的会你不让开，都是些七老八十的老右派，表示一点哀思，追思一个人有什么可怕的呢？&nbsp;&nbsp;<br />
<br />
　　我说，在五柳村发表了关于追思会的消息，可不久便被封了，连消息也不让说出来呢。&nbsp;&nbsp;<br />
<br />
　　王先生说，他们的这种下三烂手法也不创新。三年前吧，有个对包遵信的追思会，也是停水停电，你用点别的方法呀。我看了你们录的电视片，你妈给我的光盘，这种现场的再现，还没有过。你再如何讲也没有用了，你无法辩解，这都是实在的事。&nbsp;&nbsp;<br />
<br />
　　艾说：没有什么可商量的。这是冲突性的，香港说这是高调维权。这个二十层的楼全没有人了，留下了一个空楼哇。不管吧，上边不同意，管不知如何办。&nbsp;&nbsp;<br />
<br />
　　王说：从你对地震学生的调查，我开始关注你。&nbsp;&nbsp;<br />
<br />
　　艾说，我们下去调查的人，主要是员工，还有网上志愿者，他们被抓了三十多次呀。&nbsp;&nbsp;<br />
<br />
　　王问：你在国外这样对你的报导，回国后有没有感到压力呢？&nbsp;&nbsp;<br />
<br />
　　艾说：没有，我还没有感到有什么压力。因为国外国内是两条线，官和民是两条线。我在国外情况通过大使馆，会报外交部，外交部会报国务院的。麻烦太大了，报导我是这么大的照片。影响太大。国际上看得很清楚。他不敢跟我对话，因为他们都是没有理的。只是国外回来后在我的门口按了两上电子眼。我们的活动，都会录下来的。&nbsp;&nbsp;<br />
<br />
　　王问，你在业务上没有影响吗？&nbsp;&nbsp;<br />
<br />
　　艾说，我的业务都在国外，在国内影响不了我，我的业务非常好。&nbsp;&nbsp;<br />
<br />
　　王问到鸟巢的事。他说，这是我的概念，就这是个什么样子。他们团队再去发展。没有我的概念就没有这个鸟巢。但我提都没有提，我第一个宣称我不去参加奥运会任何活动。因为我原想这是全民欢乐的节日，却变成了政治宣传，这太无聊了，我很讨厌这种作法。至今我也没有去过鸟巢。&nbsp;&nbsp;<br />
<br />
　　说到了他的同学张艺谋红得发紫，社会上说，他成了官方用的艺术家。这次国庆节晚上的节目，包括胡锦涛下来与民众跳舞都是张设计构思的呢？&nbsp;&nbsp;<br />
<br />
　　艾笑道：这里边存在构思吗？我和张是同学，一届的，我们很熟悉。这都可以理解。他们如何折腾，都很正常，都可以理解，你拥有一切了，所有的资源，连法律都在你的手上啦。但要给别人一点空间，不能不让别人说话。&nbsp;&nbsp;<br />
<br />
　　我们为未未担心，高瑛更担心他的儿子，对艾未未说：儿子呀，你不让妈妈担心才好呀。&nbsp;&nbsp;<br />
<br />
　　艾未未笑着对妈妈说：妈妈，这是自私的想法呀。我是你的儿子，我还是人民的儿子呀。他转过脸对我们说：我根本不怕它们。有什么可怕的呢？为了公正平等，多少革命先烈为了这理想抛头颅洒热血，我的父亲蹲过国民党的监狱，他被流放过；还有多少孩子，几千孩子死了，他们是无辜的，谁为他们说话？哪这么简单？我个人算什么呢？我怕什么？他们最怕的就是你不怕他！&nbsp;&nbsp;<br />
<br />
　　我们说，你有这样的勇气太可贵了。&nbsp;&nbsp;<br />
<br />
　　艾未未说：我最不缺少的便是勇气！我1993年回国的时候就想好了，出国十二年，我没有拿美国的护照。我要回到自己的国家来。&nbsp;&nbsp;<br />
<br />
　　高瑛感叹着：我的这个儿子多么爱国呀，他本来可以有很好的生活的，他就是为了爱这个国家才这样做的呀，为什么误会这么深啊！&nbsp;&nbsp;<br />
<br />
　　艾未未说：妈妈，不对，这不是误解，这是正解！他们很明白。你说是误解还把他们看成正义的一方。双方都看得清楚，根本原因是利益问题。这三十年来，他们把国家的大部财富掠夺了，成了权贵集团。双方都清楚，没有误解的。没有利益不会这样的。本来，你是共产党就应襟怀坦白，大无畏的，可他们却不敢与你对话，不敢与所有的人正面对话，成了下三烂！我们是十几亿人的国家啊。不能让人民生活在这样的状态下呀。&nbsp;&nbsp;<br />
<br />
　　艾未未说：我们要把那个电视片做个完成版，二万个完全赠送。这一拳被打得很值。说到谭作人案，审了六十次，还没有判。他们怎么判呢？他只是为了收集遇难学生名单，这如何成了颠覆国家政权呢？难道你的政权就是和真相对立的吗？我为了这件事，是要做到底的，我不会因任何阻力而停下来的，我义无反顾。五一二死了这么多人，你说不清楚；毒奶粉事件，害了三十多万孩子，你只判个老板董事长，你自己都不敢承担，你就不敢担责任。你事实求是嘛，你都不敢。&nbsp;&nbsp;<br />
<br />
　　王说，所谓政治家都是没有良心的。你要自己注意。&nbsp;&nbsp;<br />
<br />
　　艾说：老毛说过，要奋斗就会有牺牲，死人的事是经常发生的。我不怕牺牲的。&nbsp;&nbsp;<br />
<br />
　　我对高瑛说，跟你儿子谈话很受鼓舞，他无私无畏，从他身上看到了希望。可惜这样的人太少了。&nbsp;&nbsp;<br />
<br />
　　艾说：每个人都要说出真话来，如果都这样，他们就完了。不要觉得没有用。&nbsp;&nbsp;<br />
<br />
　　高瑛说儿子和儿媳妇都这样，多么善良。儿媳的父母都是沈阳鲁艺的教授。&nbsp;&nbsp;<br />
<br />
　　我向艾未未讲到我为百姓维权的经历。他说，现在比你那时候更严重了，更难维权了。太多了，没法写了，你的书现在也难出了。&nbsp;&nbsp;<br />
<br />
　　艾未未说：现在是各地不能上访，所有的上访都被抓回去，因为一票否决，你这地方有上访，你就会被否决，所以他们拚命抓上访的。为了杨佳案，我们有四个人给高级人民法院送一封信，结果被抓了，让各省带回去。一个人是新疆的，他说，你抓回去，我还会回来的。因为我就住在北京。各省要花车票钱。这是很荒诞的事情。不会太久了。只要每个人说一句话它们就完了，可没有办法把话说出来。都什么时代了，还搞这个。&nbsp;&nbsp;<br />
<br />
　　王说，社会上对你的关注，是因为你是有名的艺术家，你的名望很重要哇。你在几个领域，都有影响，出这个事情，所以被万人注目。还有你是大诗人艾青的儿子。&nbsp;&nbsp;<br />
<br />
　　艾未未说，谭作人案的律师是浦志强。有三拔证人，都不让出庭。去年北京吊销了四十多律师的执照，为百姓说话当律师也不容易。小浦是个很不错的律师。公盟也被罚款。他们这样做并不奇怪，不这样做便奇怪了。&nbsp;&nbsp;<br />
<br />
　　我说到与中央党校老校长高扬的谈话。高扬提出，中国最大的问题是没有民主，把民主倒过来变成了主民。这样下去，能不能如苏联那样维持七十四年都很难说。&nbsp;&nbsp;<br />
<br />
　　说到在德国治病事，艾未未母亲问：你这次花了不少钱吧？&nbsp;&nbsp;<br />
<br />
　　艾未未说：是德国的一个议员知道我的病，给这家医院的给我动手术的医生打电话，这位医生是德国最好的，他对我说：你的手术是我送给你的。这钱我是花得起的，但人家这样说，我就接受了。在外国都是有道义的，只有中国没有，哪有先要钱才能治病的。要先治病才对。中国老百姓看不起病，你要死了也要先交钱才给你做手术。&nbsp;&nbsp;<br />
<br />
　　我问他如何在法律上为自己事申诉？艾说：我已经向成都公安局递了申诉要求。他们说因为复杂，要我再等些天。到今天还有十四天时间，等他们的答复。&nbsp;&nbsp;<br />
<br />
　　我说，你们父子俩是一部书。&nbsp;&nbsp;<br />
<br />
　　艾未未说，我们父子都有反骨吧。可我的爷爷是江南的小地主，开明士绅。是个特别忠厚老实的人哪。我母亲是非常善良的，我记得小时候，在青海，她看到一个要饭的小孩子，要我把我的棉衣给他穿，我那时非常不情愿啊，我的棉衣为什么给他呢。可妈妈还是叫我脱下来了。&nbsp;&nbsp;<br />
<br />
　　艾未未在他的母亲身上继承了这种善良，这是人道，是同情心。&nbsp;&nbsp;<br />
<br />
　　王先生对高瑛说，你嫁给了这样的丈夫，又有了这样的儿子，这是你的自豪和骄傲啊。儿子身上集中了父母的精神啊。&nbsp;&nbsp;<br />
<br />
　　说起开放初期艾未未他们搞星星画展的事，我说到了与他一起搞画展的王林的儿子，艾未未高兴地叫出名字，是王克平呀，他是我的好朋友。我说给未未，在十一月四号在文学馆举办王林百年诞辰王林文集讨论会。未未立即接通了法国的克平，艾未未电话说：你父亲在十一月四日有百年纪念，你回国参加吗？你不回来，我去算什么事呀。这儿有你父亲的朋友，你和他说说话吧。&nbsp;&nbsp;<br />
<br />
　　于是我接过了未未的手机，与远在法国的克平通了话。克平管我叫叔叔，我纠正说，你父亲比我父亲大两岁，我管他叫伯伯，和与你父亲在1946年就认识的，我到你家去，你还小。我是你的哥哥吧。你何时来北京我们再见面吧。&nbsp;&nbsp;<br />
<br />
　　我把手机又交给了未未，他接着说：我们差一点见不到了哇，我的头上打了两个眼儿。&nbsp;&nbsp;<br />
<br />
　　放下电话，艾未未让我说起王林的历史和他的写作悲剧。&nbsp;&nbsp;<br />
<br />
　　王先生说起这个房子的事，艾未未到这儿有十多年了。我们有三十年的租用合同。听说，这儿要拆迁了。这附近的不少画家的房子都是艾未未为他们设计的。可现在说拆就要拆，前些日子他们进来丈量了，说这儿在拆迁。说要在这儿建多少剧场，搞成百老汇。这儿变成文化区是不容易的，你说拆就拆，不和人商量。政府通过拆迁好得一笔钱。城里的拆迁，农村的土地，是全国的大问题。这在国外是不能随便拆迁的。也是不能随便进人家院落的。我们也许要到天安门前说说理的。&nbsp;&nbsp;<br />
<br />
　　王先生说，这要国外可以诉上法庭，有个说法，我的房子虽破，你国王也不能随便进，叫风能进，雨能进，国王不能进嘛。你就是拆迁有理，你也得给人时间哪。土地问题是大问题&nbsp;&nbsp;<br />
<br />
　　高瑛说：这是国家的土地，一切说是国家的，其实都是党的。国家的事情家里办。是一些人家里处理，哪有一点民主啊。&nbsp;&nbsp;<br />
<br />
　　艾未未说：这回在德国法兰克福书展上，作家协会主席铁凝在会上说：中国是最有写作自由的，没有一个作家是因为写书写作而被抓捕的。她的话一出，招来一片骂声。这不是昧着良心说话吗？&nbsp;&nbsp;<br />
<br />
　　说起在地震中发言的作家余秋雨，他含泪劝告，把自己搞得很臭；艾未未气愤地说出一个名字，是山东省一个作家王占山，竟写诗把孩子的死说成上了天堂！一位川北的母亲给艾未未的信中写着：我惟一希望做的是让更多的人知道我的女儿曾经快乐的在这个社会活了七年，我希望大家记得她的名字，记得所有遇难同胞的名字。&nbsp;&nbsp;<br />
<br />
　　艾未未说：我们是一个一个到家里调查的。&nbsp;&nbsp;<br />
<br />
　　艾未未说：政府所有的信息应当公开。今天还要进行公民启蒙，这要做好多准备工作，目的就是让更多的人知道。关系到地球上有六分之一的人的生存状态。我就是明确地说，这种极权必须结束。我们看看六十年前，共产党向全国人民的承诺，我说，是谁背叛了当年的承诺？当年骂一党独裁者，提出了要实现民主自由，提出军队国家化，不能成一家的武装。哪一篇文章，你们今天敢于再读一下？我看了，觉得很好的话，美国纽约日报记者采访我，我就说了是谁背叛了自己。我也许想得简单，简单也有力量。其实真理都是很简单的，没有那么复杂。&nbsp;&nbsp;<br />
<br />
　　王说：有一本书就是集录了六十年前共产党的文章，这本书叫《历史的先声》命运很惨。不许出版，出版社还受了处罚。你是艺术家还是思想家，不简单哪。&nbsp;&nbsp;<br />
<br />
　　艾说：我其实很简单。也许这简单也有力量。他们本身是腐朽的。我们不怕他。但他们如采取黑手段，我们就不行了。&nbsp;&nbsp;<br />
<br />
　　王说，我对你的团队很佩服。&nbsp;&nbsp;<br />
<br />
　　艾说：这都是志愿者，还有很多人想来参加，说不要工资也愿意来。正因为他们得罪了很多人，很多人是有正义是非感的呀。&nbsp;&nbsp;<br />
<br />
　　我问到未未，你要国外听到了对中国国庆的看法吗？外国如何看这次六十年大庆的？&nbsp;&nbsp;<br />
<br />
　　未未说：这完全是北朝鲜的作法，莫明其妙的。这样耀武扬威，这样举着四人的像，看不明白这是要做什么。你对外国，哪怕是很小的国家，你都害怕。你这样搞花了多少钱，有人说是一千七百亿，这是要向人民说清楚的。只有两三个人提出了这样的问题。信息应该公开呀。在外国花钱是要通过议会的，我们通过人大了吗？这样的事我就要求它信息公开，你花了多少，花在了什么地方。这是法律，他不能不理会。我有个深圳朋友就要做这个事，他向各部委发信。&nbsp;&nbsp;<br />
<br />
　　信息公开条例，十五天要公开。我们提出了多少个问题，要让政府公开。这是公民的教育。要做很大的努力，目的是把话说清楚。我就不信做不成，必须有人做，如果你不做，便永远做不成。&nbsp;&nbsp;<br />
<br />
　　妈妈说，你这样做会说你也在颠覆国家政权呀。你是高瑛的儿子，妈妈为你担心哪。我活了七十七岁了，我死是不怕了。可你呢？你爸爸说他去见马克思去了，我是不去他哪儿的。你搞阶级斗争，我不想搞。儿子说，搞阶级斗争是从列宁哪儿来的。中国党其实没有多少真正的马克思。&nbsp;&nbsp;<br />
<br />
　　未未说：我的观点明确，极权制度必须结束。一要承认你的罪恶，二要结束你的政权，没有商量的。我这样做是被逼的。我本来是可以在国际上有话语权的。但看到中国的现实，我必须这样做。&nbsp;&nbsp;<br />
<br />
　　我们说，这是你的责任感哪。这是最可贵的。&nbsp;&nbsp;<br />
<br />
　　未未说，其实我是最没有责任感的，是现实逼我这样做的。&nbsp;&nbsp;<br />
<br />
　　艾未未说：中国八零后的韩寒，这个年轻的作家，就很了不起，这就是中国的希望。他的文章一天就点击多少万哪。&nbsp;&nbsp;<br />
<br />
　　谈了半天，我们要离开了，我要艾未未的电话，他说我给你名片吧。我有电子信箱，不过都是监控的。不过也不怕，我都是打开天窗说亮话，不怕它监听的。&nbsp;&nbsp;<br />
<br />
　　未未领我们到了他的办公室去。好大的一间办公室啊。这一面宽大的墙上贴着满一面的死难孩子的名单。我走近跟前，看着打印出来的小字，有姓名，出生年月，在哪个小学中学读书，在哪天死去。我的心灵受到巨大的震憾：我读着这孩子的名字，这是父母的希望，这是祖国的花朵，他们就这样在我们面前，向我们发出质问。对他们的死去，我们能做的便是如未未所做的了。我看到最后的数字是5193。这是以上孩子的总计。&nbsp;&nbsp;<br />
<br />
　　面对着这五千多花朵般的孩子，面对着未未所做出的一切，我不由得感到深深地愧疚。为我的曾有过的动摇，为我曾不时的恐惧，也为我的自私和懦弱。&nbsp;&nbsp;<br />
<br />
　　走出这间办公室，我们到了未未本人的工作间，在宽大的屋子中间，有一座小山，是什么堆积如山呢？我近看，大为惊奇，原来是山一样高的葵花籽呀。我抓起一把，呀，沉甸甸地，未未说，这是石头，是用人工制造成葵花籽，每个籽上都是用笔划上去的纹理，是这样像，这是什么意思？&nbsp;&nbsp;<br />
<br />
　　我拿起了一把，不想放下，我要把它保存起来。我把这石头般坚硬的葵花籽放在心头。葵花籽，你会在人心里开花吗？你会开放出永远向太阳的花来。这是用石头般坚强的种子种在心里的花朵。这就是未未的性格，这就是未未向太阳的精神吧。&nbsp;&nbsp;<br />
<br />
　　愿亿万个这样的葵花籽在人心里生长开花。</div>

							
							
							
							
