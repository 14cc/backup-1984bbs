朱学勤：我时常想起鲁迅，胡适与钱穆<br />
<br />
2010-1-20<br />
<br />
&nbsp; &nbsp; 我时常想起鲁迅，想起胡适，想起钱穆，不太想得起梁实秋，林语堂，周作人。&nbsp; &nbsp; <br />
<br />
　　对鲁迅，我的认识有过反复，感情上有过起伏。60年代至70年代是信奉，80年代则是怀疑、疏离，甚至有些厌烦。80年代最后一年起，才明白自己所处的年代还是鲁迅的年代。&nbsp; &nbsp; <br />
<br />
　　在片面信奉的年代所形成的读者与作者的关系，无异于一场包办婚姻。除了意识形态读物，你能够读够读到的另一种读物就是鲁迅，你对于20世纪上半叶的了解如果不满于教科书的灌输，那就去读鲁迅全集后面的注解。由此产生的热爱，是盲目的热爱，没有经过选择的热爱，与包办婚姻有什么两样？包办婚姻是不牢靠的，很容易被第三者插足。80年代一来，有多少精神世界的新鲜第三者打将进来？由此产生包办婚姻破裂，出现另一种选择，完全正常。经受了80年代的冲击，还固守原来的状态，并不令人尊敬，而是一种很可怕的状态。&nbsp; &nbsp; <br />
<br />
　　80年代结束，所有搅动起来的东西开始沉淀下来。这时逐渐对鲁迅发生回归，发生亲近。此时回归，可以说是痛彻心肺之后的理解。他那肃杀的文风，我一度以为是他个性使然，后来方明白是那样的现实环境逼出了那样的文风。他正是以那样的文风忠实地反映了那个时代的黑暗。反过来，现在读林语堂，读梁实秋，你还想象就在如此隽永清淡的文字边上发生过“ 三・一八” 惨案，有过“ 民国以来最黑暗的一天” ？当然，在那样的心境中，鲁迅也消耗了自己。他是做不出也留不下钱钟书那样的学问了。&nbsp; &nbsp; <br />
<br />
　　我怀念鲁迅，有我对自己的厌恶，常有一种苟活幸存的耻辱。日常生活的尘埃，每天都在有效地覆盖着耻辱，越积越厚，足以使你遗忘它们的存在。只有读到鲁迅，才会想到文字的基本功能是挽救一个民族的记忆，才能多少医治一点自己的耻辱遗忘症，才能迫使自己贴着地面步行，不敢在云端舞蹈。此外，还有一个私心所为，那就是对文人趣味的厌恶。这可能是我的偏见。&nbsp; &nbsp; <br />
<br />
　　在鲁迅的同时代人中，多多少少都会读到那股熟悉的气味，惟独鲁迅没有。而鲁迅，本来是比他们中的任何一个都更有资格过上那种精巧雅致的人文生活。在鲁迅的精神世界里，通常是文人用以吟花品月的地方，他填上的是几乎老农一般的固执。他是被这块土地咬住不放，还是他咬住这块土地不放，已经无关紧要。要紧的是，他出自中国文人，却可能是唯一一个没有被中国的文人传统所腐蚀的人。&nbsp; &nbsp; <br />
<br />
　　这是一件很平淡的事，却应该值得惊奇。&nbsp; &nbsp; <br />
<br />
　　我曾经以俄国的车尔尼雪夫斯基、别林斯基和陀斯妥耶夫斯基的高度苛求过鲁迅。后来才明白，在一个没有宗教资源的世俗国度，鲁迅坚持在那个世俗精神能够支撑的高度上，已经耗尽了他的生命。想想看，中国人成天念叨鲁迅，有无一人敢于继承他的精神、他的风格？仅此一点，就说明了全部。人人都能谈鲁迅，却是把鲁迅高高挂起，把人晾在高处，任其风干。鲁迅的生前并不快乐，鲁迅的死后更为凄惨。&nbsp; &nbsp; <br />
<br />
　　鲁迅是留下了缺憾的。&nbsp; &nbsp; <br />
<br />
　　现在知识界用以平衡鲁迅的是梁实秋，是林语堂，是周作人。而我以为，真正能够平衡鲁迅，在鲁迅之外树立另一价值坐标，同时也不辱没鲁迅的是胡适。&nbsp; &nbsp; <br />
<br />
　　胡适的一生是坚持自由主义的一生。难能可贵的是，他是以与这一信仰相匹配的温和态度坚持了60年，同时不失坚定。他既未被那个时代所激怒，在激怒中一起毒化；又未被逃避那一时代的文人情趣所吸引。他完全有理由走向这两极的某一极，但是这个温和的人竟然做到了某种倔强性格做不到的事情--始终以一种从容的态度批评着那个时代，不过火，不油滑，不表现，不世故。仔细想想，这样一个平和的态度，竟能在那样污浊的世界里坚持了60年，不是圣人，也是奇迹。胡适的性格，与这一性格生存的60年环境放在一起，才会使人发现，也是一件值得惊讶的事。&nbsp; &nbsp; <br />
<br />
　　胡适学术建树一般，但大节不坠，人格上更有魅力。鲁迅生前对他有过苛评，但鲁迅死后，当後人问及胡适对鲁迅的评价时，胡适却告诉来者，不能抹杀周氏兄弟在近代文化史上的独特贡献。雷震一案发生，胡适原来对雷震那样的活动方式有保留，用今日某些人合情又合理的标准，胡适完全可以袖手旁观，指责雷震犯了“ 激进主义” 病症。谁也没有想到，当被问及对此事的反应时，胡适竟然那样动了感情。他当场以宋人杨万里诗《桂源铺》作答：&nbsp; &nbsp; <br />
<br />
　　万山不许一溪奔，&nbsp; &nbsp; <br />
<br />
　　拦得溪声日夜喧。&nbsp; &nbsp; <br />
<br />
　　等到前头山脚尽，&nbsp; &nbsp; <br />
<br />
　　堂堂小溪出前村。&nbsp; &nbsp; <br />
<br />
　　我曾与一位学界老人谈论此事。老人当时病卧沉榻，突然从床上坐起，口诵此诗，热泪盈眶！&nbsp; &nbsp; <br />
<br />
　　我还时时想起钱穆。《八十忆双亲、师友杂忆》，那样的书名，未及开卷，就让人体味到儒家的生命观照，是那样亲切自然：身体发肤受之父母，精神生命则发育于师友。两种生命皆不偏废。&nbsp; &nbsp; <br />
<br />
　　学者需钱穆的学术专著，一般读者仅钱穆回忆录即可获益匪浅。钱穆以研究中国文化史著称，他的回忆录本身就提供了一部中国近代文化变迁的可信注释。&nbsp; &nbsp; <br />
<br />
　　钱穆没有读过大学。但是他在苏、锡、常度过的小学、中学生涯，同学中有刘半农、陈天华、瞿秋白，教师中有吕思勉等，一时人文之盛，令今天的牛津、剑桥的博士都羡慕不止。1941年夏，他回乡省亲，当时声望已不在吕思勉之下，吕思勉邀其回常州第五中学讲演，钱穆恭敬从命。一代国学大师，与当年的师长比肩而立，竟句句以学生自居。他谆谆告诫那些年轻的校友：此为学校四十年前一老师长，带领其四十年前一老学生，命其在此演讲。房屋建筑物质方面已大变，而人事方面，四十年前一对老师生，则情绪如昨，照样在诸君之目前。此诚在学校历史上一稀遘难遇之事。今日此一四十年前老学生之讲辞，乃不啻如其四十年前老师长之口中吐出。今日余之讲辞，深望在场四十年后之新学生记取，亦渴望在场四十年之老师长教正。学校百年树人，其精神即在此。&nbsp; &nbsp; <br />
<br />
　　钱穆在学问上与新文化运动分道扬镳，但是他公正地感谢是新文化运动的中坚人物提携了他。顾颉刚回苏州探亲，发现了钱穆的才华，推荐他进燕京大学任教。一个没有大学文凭的中学教师，一步登上了大学讲台。后来他与胡适失和，但并不影响胡适聘他任北大教授。所有这些回忆，反过来该能纠正一些时令学人对新文化运动及其人物批评过盛？&nbsp; &nbsp; <br />
<br />
　　鲁迅，胡适，钱穆，三人之间，一个与另一个相处不睦，然而他们却构成了 30年代知识界的柱梁。我们是喋喋不休地重复梁实秋的雅舍、周作人的苦茶、林语堂的菜谱，还是老老实实地告诉我们的学生，我们曾经有过鲁迅的社会批判、胡适的自由思想与钱穆的严谨学业？三者合一，应该成为我们向学生介绍30年代知识分子的三种主要形象。那是一个已经逝去的铁三角，他们凝视着这个轻佻的当下，沉默不语。</div>

							
							
							
							
