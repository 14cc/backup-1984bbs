																											<div id="postmessage_450314" class="t_msgfont">朱学勤：鲁迅的思想短板<br />
70年后谈鲁迅―――已经是争议而不是盲信，这一步来之不易。倘若鲁迅活到今天，他首先高兴的应该是争议，而不是磕头作揖。他留给后人的遗产是精神，不是商业或学术利润。满城鲁氏广告，以及为商业利润阻止人们对鲁迅的争论，只能证明鲁迅生前即开始疏离那座城市，确有几分远见。这个人也不会希望围绕一本书、一个人形成一个“学科”，养活那么多文学教授，这从他当时对“红学热”、“红学界”保持距离，大致可以推断。亦可见他如何嘱托后事：第一，后代不可做文学家，尤其不可做空头文学家。所谓“空头文学家”，倘不伤害文学，一个最浅表的解释是，没有文学作品的文学家，以及通过文学评论做成的“思想家”。第二，希望他的文字“速朽”，“死后埋掉”，“拉倒”。大文豪留下如此遗言在文学史上十分罕见，几乎是一份反文学遗嘱，口气之绝决，显然反映他对一生从事的文学事业是如何绝望，此外，也是要堵死身后文学界对他各种方式之利用。前一点周海婴做到了，后一点许广平没有做到，形格势禁，她也做不到。如果鲁迅能看到自己的遗骸，上面爬满“鲁学家”而且是密密麻麻，由此产生与“红学界”紧邻的一个“鲁学界”，这个本身就活得不快乐的人，只会在死后增加一层痛苦，痛苦得抽搐。 <br />
<br />
　　鲁迅精神不死，能够活到今天的遗产只有一项：对当权势力的不合作。胡适晚年曾回顾五四之后分手的两位同道，说他们倘若活得足够长，一定会殊途同归。说陈独秀的那一句很对，说鲁迅的那一句则靠不住。即使鲁迅与胡适齐寿，能证明“是我们的人吗”？这句话对一半，错一半。对的这一半，对应鲁迅这一份精神遗产。错的那一半，正好与当今“鲁学界”所回避者重合，大多可商可议，甚至是可歌可泣――― <br />
<br />
　　经济学解释。读鲁迅日记，恰如读鲁迅遗嘱，满篇皆为“阿堵物”，令文学青年添堵。毛泽东对鲁迅赞不绝口，但他肯定不会有耐心读完鲁迅日记。尽管鲁迅和他一样忽视经济自由，尤其是制度层面的经济自由，但是鲁毕竟与他有一点不一样：毛个性张扬，支撑他的是天子特权，鲁迅个性独立，要依靠他自己小心打理财务，并没有“特别会计室”在身后伺候。于是我们看到毛泽东能攒下200万稿费，这在消灭私产的年代，几乎是中国最大一笔私有财产，而鲁迅每天晚上却要在灯下记录钱进钱出，一丝不苟。鲁迅反对当时的经济制度，但那样的制度在形而下层面却支撑他完成写作，他一刻也离不开那样的制度。他不避后人毁誉，认真记录“阿堵”，这一点做得很磊落，至少能证明他生前就没有死后可登祭坛的小九九。这些形而下记录是被一个经济学家揭示的，难以进入文学家的视野，他们还在悲愤地说：鲁迅吃的是“草”，吐出的是“奶”！这部日记却告诉后人，鲁迅吃的不是“草”，而是“奶”，而且是自由经济的“奶”，而不是统制经济的“草”，否则，我们就不会有“鲁迅”，只会有“鲁学界”。 <br />
<br />
　　教育史解释。中国是在1905年废了科举，不到一代人的时间里，科举制废墟上突然矗立起全新教育体系，这是本国教育史的奇迹，也是世界教育史的奇迹。这一现代教育体系，既有民国初年一代“海归”通体合作，也有英美派与日俄派的迅速分裂。分裂从属于更为宽广的世界史背景，但在当时却烙有很local的省籍情绪。鲁迅前半生是这一体系的受益者――脱离教育界多年，还享受教育部300银元的“奶”，但就整体言，他是这一体系的出局者。他后来对大学体系的刻薄嘲讽，未必是文化批判的精品，但从教育史来读，却是一份不可多得的史料，可征可信。<br />
社会学解释。上述分裂与社会转型有关，昔日转型今日再次启动，因为有重演，反而能更清晰地看清前一幅画面。邓小平解决香港回归提出“一国两制”，但就大陆本身言，却是“一制三国”：沿海中国、内陆中国和边疆中国。社会转型无论是初次、再次，或者还有第三次，这“三国演义”都是渐次递进，从一个沿海“大外滩”扩及至内陆中国、边疆中国。“大外滩”无论发展快慢，或有无特区、租界，吸收能力总是落后于内陆与边疆失学、失业者的蜂拥而入，两者之落差，当年是革命源泉，今日成网络口水，“愤青”变着脸出现。只不过当年的“愤青”按照“政治正确”，是一定要称为“文学青年”或“进步青年”的，那时尚无“愤青”、“愤老”此雅号，也没有“北漂”、“南漂”之分途。胡适乃五四之父，只能满足“余永泽”，不能吸引“林道静”，跟不上五四之后越演越烈的“丘九”潮流(“丘八”之后有“丘九”)，那是他活该倒霉，命中注定“暴得大名”者，必有社会学困境。鲁迅之不幸，是在上述争逐中出局，鲁迅之精敏，是他始终不离开沿海中国，就在“大外滩里”守一个小孤岛―――租界“且介亭”，这就守住或开辟了沿海中国里的内陆中国。试看萧红、萧军之秉性，今日北大、复旦中文系的旁听生中并不乏见。“林道静们”今天是要受大学正途歧视的，于是就到网络上去泄愤，但在当时却有一个“且介亭”可以挤进去取暖。那时在上海发生报刊文人与教授集团之争吵，这一恩怨延伸到“文革”初期才有个了断。1966年是左翼文人从报刊上发难开始，讨伐大学里“资产阶级反动学术权威”，这一幕可解为百年棋枰，内陆中国对沿海中国终于从文学上“收官”，出局者回来收拾残局，确实是“笑”到了“最后”，“笑”得“最好”。那时当然有毛泽东更为深刻的政治谋略，以及1949年以后特有的意识形态话语，但就历史纵深言，“大批判”发端于报刊，教授集团在大学校园里戴高帽敲锣，这两幅画面皆可从1930年代那一个回合中找到遥远伏线。 <br />
<br />
　　政治学解释。这方面说得最多、评价最高者，非毛泽东莫属。尽管他知道鲁迅生前在他诗词中读出过“山大王气”，但还是不吝笔墨，讴歌鲁迅为“空前的思想家”、“空前的民族英雄”。这么高的评价，今天连鲁迅的后人都不太好意思在70周年纪念中再次引用。但毛泽东也是坦率的，比所有的“鲁学家”都可爱。1957年在上海，他坦言如果鲁迅还活着，要么是保持沉默，要么是呆在监狱里，这句话能给毛泽东与鲁迅的政治关系盖棺定论。鲁学界现在很为难，为维护这两面，尽可能不损害其中一面而大费周章。鲁迅晚年一再声明他是要听“将令”，写“遵命文学”的，如此支持“左联”事业，为什么这一支持只能被利用，而不能贯穿始终？也许可以这样辩护，他们也确实这样辩护了：这能证明鲁迅追求的是民主，故而胜利后才有那样的困境。如此“民主”辩护，不符合鲁迅思想原貌―――他生前信仰的“超人意志”而不是“愚民当家”；同时也损害毛泽东形象―――既然称人为“空前的民族英雄”，为什么要让这样的“民族英雄”胜利后保持沉默？即使牺牲毛泽东形象，也还是回避不了对鲁迅的诘问：五四那一辈他有很多同道，为什么其他人的民主追求不可能被利用，惟独鲁迅能？这一问题已经出现，毛称鲁迅的骨头最硬，谢泳发问：为什么最硬的骨头会成为打人的棍子？辩护者说，这是鲁迅身后事，与鲁迅无关。若果我是谢泳，一定追着问：胡适未见得“软”，陈独秀则更“硬”，同样也有一个“身后”，为何他们没成为、也不可能成为打人之“棍”？ <br />
<br />
　　思想史解释。作为“空前思想家”的鲁迅，短板恰在思想，而不在其他，他的杂文艺术确实是最好的。鲁迅之坦率不亚于毛泽东，曾坦言他的思想支撑是“个人的无治主义”，用今天的话来说，就是“无政府主义”。这是思想史上很严肃的一个学派，19世纪曾与马克思主义同床共眠，虽同床异梦，却为后者争取到数不清的激进青年，尤其是文学青年。无政府主义席卷五四一代，是重演19世纪欧洲思想史的老版本，也是为马克思本人深恶痛绝的一幕。那时是马克思主义第二次进中国，并不是第一次，为什么能突破辛亥前第一进中国之困境？这就要感谢而不是憎恶无政府主义。这是落后国家转型期屡见不鲜的社会学现象，并不仅限于中国。无政府主义不仅与马克思主义同床异梦，也与这一类地区的民主追求同床异梦，甚至是以更决绝，因而也更具审美价值的方式，与后者貌合神离。这一块思想短板，存在于鲁迅，也存在于鲁迅生前身后诸多信徒和欣赏者。终其一生，鲁迅不屑于代议制民主，自然要与胡适决裂，也自然会被毛泽东激赏。至于毛泽东的浪漫无政府主义怎么会与气象森严的列宁主义相结合，则是思想史上比鲁迅较为烦杂的另一问题。但在这里可以先提一笔：无政府主义与马克思主义是思想上的貌合神离，与列宁主义则是政治天敌，能把无政府主义者送入监狱的是后者，而不是前者。鲁迅信徒前期如萧军、后期如胡风皆遭整肃，秘密潜伏在这里；鲁迅本人活到1957年也只能进监狱，秘密也预设在这里。毛泽东进城前激赏鲁迅，缘于他们的共同气质―――“个人的无治主义”，进城之后突然坦言鲁迅继续写，只能进监狱，那是出于他的列宁主义。鲁迅毕竟是文学家，弄不懂“个人的无治主义”与马克思主义是貌合神离，也因为他去世太早，看不到他与列宁主义只有1957式的政治关系。惟毛泽东一拍两响，既能激赏鲁迅，也不妨碍他坦言无忌，把心爱的鲁迅如何如何。马克思主义也好，列宁主义也罢，再加一个“个人的无治主义”，这三者亲密碰撞，会发生惨痛迸溢的光芒，具有强烈的美学效果，正是这种悲剧性美学效果，被各国“林道静们”误认为是民主悲剧，其实是哪里归哪里，俱不相干。<br />
<br />
<br />
这个“主义”不仅能解释鲁迅的思想结构，也能解释“鲁学界”新提出的一些观点：其一，鲁迅身前已经感觉此类革命一旦成功会对他不利，留有一些要“出走”的警告。其二，鲁迅晚年加入“左联”，是要在“左派内部造反，以激活左派政治”。要解释前一点并不难：这不正是继续革命、不断革命的心理起点？革命尚未结束，革命人已不耐烦，毛泽东恰恰是在“文革”前夜感到孤独，生平第二次达到怀念鲁迅的高潮，抄录迅翁诗曰：“万家墨面没藁莱，于无声处听惊雷”。后一点也不神秘：无政府主义的思想性格就是如此，历史上并不是第一次这样干，可以说一贯使然，19世纪欧洲思想史不断上演这一版本，普鲁东在法国左翼中这么干，巴枯宁在俄国左翼中也曾这么干，都是在“左派政治中造反，以激活左派政治”。无政府主义是比“左”，比左还要左，要不然马克思面对这些思想盟友就不会那样烦躁不安。此外，这一“主义”还能部分解释“鲁迅的骨头硬”，这是因为他与民主不相干，聚焦于意图伦理，而不是责任伦理。民主与责任伦理相伴，顿失滔滔，与美学效果无缘，不可能让文学青年称心如意。一旦与鲁迅相比，胡适确实暗淡，这是他除了上述社会学困境外还要有第二宿命：美学宿命。此外，无政府主义调动的是反专制情绪，反专制未必是反过来的民主，很可能是专制之候补。专制者之来源，首推世袭，可惜血缘稀少，但它可以从对立面吸取候补，这就大大增加“造血细胞”，能突破数量限制。无政府主义在思想史上很长寿，通常是情绪偏强思维偏弱，因为是“闹情绪”，故而容易入门，生生不息。19世纪曾让马克思烦躁的东西，21世纪却绕到了他视野背后，成为他身后殿军，很可能是中国的“后现代主义”？后现代无论是把格瓦拉、鲁迅搬进“剧场”恶搞，还是在书房里作“理论长考”，最底下一层还是当年的情绪。故而今天中国的左翼文人，对外拥抱后现代主义，对内拥有鲁迅后裔的悲痛神色，这两者在逻辑上恰好内洽，用不着羞羞答答。只是他们不敢像鲁迅当年那样坦率，坦言自己是“个人的无治主义”，在这方面又突然软了下去。 <br />
<br />
　　国民性改造。最后，应该谈谈文学家的最爱―――“国民性改造”问题了。 <br />
<br />
　　“国民性改造”在本国思想史上确有渊源，上承梁启超推卸戊戌变法失败之责，四亿同胞人人有份，故而提出”新民“说，今天上海《新民晚报》的报头，还能看到这块思想化石。下启毛泽东改造旧人的“文革”理想―――”红雨随心翻作浪“，”六亿神州尽舜尧“。但就鲁迅当年留学日本言，那个岛国急于脱亚入欧，又不想彻底改造制度，才会将改造热情转移至人脑：在文化改造、人性改造上狠下功夫。当年与日本同享一个家族谱系的德意志则更早，落后于西欧，不服于西欧，故而提出德意志“文化”优越于西欧“文明”这一说。从费希特到尼采，从尼采到海德格尔，“文化决定论”开出的单子很长，中国士人接上去的单子则更长。希特勒之所以能上台，是得益于这一条思想脉络的，文化革命之所以能发生，也是得益于这一思想谱系。国家社会主义(简称纳粹)推行种族清洗，是有文化“理由“的：为净化国民性，不得不然！只不过德意志傲慢，日本人自卑，前者是净化，后者是改造，两家人在上下两端共同推进“国民性基因工程”。鲁迅留学国为日本，“国民性改造”流入他血脉，无须苛责。此前此后还曾多次表述对尼采哲学的向往，“个人的无治主义”是他的政治思想，若问哲学倾向，他肯定会坦承是尼采超人意志说，而不是“为人民服务”。经过反法西斯血浴，文明世界里若有提“国民性改造”，会把人吓个半死。经过1950年代“改造”高潮，中国的幸存者也会有类似感受。听者先会问：“谁来改造，谁有权力改造？凭什么改造？” <br />
<br />
　　鲁迅当年是没有见到“改造”后果的，应该为他庆幸。毛泽东说“鲁迅的心是与我相通的”，一点没说错，不仅“个人的无治主义”与“和尚打伞，无法无天”息息相通，“国民性改造”也与“灵魂深处爆发革命”一脉相承。历史上有机会实践此类主张者，也不仅仅是法西斯这一翼。法国革命、俄国革命、中国的文化大革命，乃至红色高棉改造金边的“四一七新人”，代代有其人。另一方面，二战后出现同一块“国民性”版图分裂：东、西德，南、北韩，南、北越。民主的养成在民主本身，宪政的训练也只能在宪政，这一点倒是胡适1930年代说得对。大陆同一块土地，没有“版图分裂”，但也有两个社会阶段可举例：“文革”与改革，还原为人性论差异，前者是摁着手坚持“改造”，后者手一松，社会大还俗，国民性立刻还原，还只是政策变异，就引起多么相反的社会后果，究竟是哪一面好，那一面劣？人不是神，也没有“超人”，“人改人”只能吓死人、改死人，如今还能迷惑人，只说明中国仅有“文革”之难还不够；灾难虽然在政治层面被否定，但在思想上还没有被反思，或者干脆说：连个像样的反思都还没有。 <br />
<br />
　　鲁迅就没有任何精神可以继承了吗？当然不是。前面说过，他对当权势力的怀疑，以及由此发生的不合作，是可以继承的。但要用到左派一句老掉牙的话，应该“批判地继承”，这一精神是从他那块思想短板―――“个人的无治主义”开始的。虽然，思想史上的歪打正着，过去有，现在有，将来也会有。(此文略删)</div>

							
							
							
							
