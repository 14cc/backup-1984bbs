2月6日，英格兰足总发表正式声明，解除特里英格兰国家队队长职务。原因就在于特里与队友布里奇的妻子有婚外情。英国首相布朗对该决定表示支持。<br />
<br />
1981年，里根就任美国总统，在建国两百多年之后，美国选民才接受第一位曾经离婚的总统。迄今为止，也是唯一的一位。另外，现任法国总统萨科齐也是第一位离过婚的法国总统。<br />
<br />
在美国南方各州，有55%的学区在性教育课程中讲授婚前不能有性行为，在东北部，有20%的学区也这样做。<br />
