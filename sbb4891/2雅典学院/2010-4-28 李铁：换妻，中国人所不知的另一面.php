美国联邦最高法院至今没有做出过与换妻活动直接相关的判决。1998年，凤凰城通过了法令禁止现场性表演，使得换妻俱乐部成为非法。1999年，当地换妻俱乐部的老板及成员提起诉讼，要求法院宣布法令违宪， 2003年，亚利桑那州上诉法院最终驳回请求，确认了法令的合宪性。在此之后，德克萨斯州的丹肯维尔亦通过法令使当地的换妻俱乐部成为非法。另外，美国还有多个州，至今仍然有通奸罪。<br />
