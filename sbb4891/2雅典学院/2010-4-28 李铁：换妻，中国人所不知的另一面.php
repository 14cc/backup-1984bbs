1976年，卡特在竞选中反复承诺，他的政府一定要把重建家庭问题置于最优先地位。“美国家庭出了毛病”―这是吉米・卡特对性解放导致家庭危机的概括。入主白宫后，卡特曾五次召开专门会议，商讨制定新的家庭政策。<br />
<br />
1984年，美国《时代》周刊发表了《性解放走入死胡同》一文，指出，“审慎和责任感是80年代的格言”，反思了性解放运动的问题。法国《费加罗杂志》发表了《新的性道德：恢复忠实》一文，指出：“不负责任的婚姻时代已经过去”。 关注家庭的健全发展，已成为世界各国政府和人民的共识。联合国将1994年确定为“国际家庭年”，呼吁重视传统以及家庭的价值。<br />
<br />
<br />
实际上，60年代的那场性解放运动，只是人类历史上反复上演的乌托邦运动中的一次。在人类历史上，性放纵风潮其实并不新鲜。美国社会学泰斗索罗金这样概括：在巴比伦、波斯、马其顿、蒙古、希腊和罗马文明的后期，以及埃及古王朝、中王朝和托勒密王时代。历史反复上演：一旦整个社会放松对性的约束，则在三代之内，文化便会没落下去。<br />
<br />
60年代，加利福尼亚州旧金山市附近由一群嬉皮士创办了“群居公社”，群居男女随便发生关系，生了孩子不知道父亲是谁，他们就由一个委员会来抚养。此后，全美各地陆续建立的“公社”和“群居村”多达三千个左右。但这种公社很快穷途末路，纷纷解散。<br />
<br />
这些“解放”运动失败的原因很简单：性并不是握手、做运动那么简单，高潮并不是性的惟一意义，我们无法对那些关于性的复杂而深刻的人类情绪装聋作哑。这几乎是由人的本质天然决定的。<br />
<br />
人类历史上的主流文明对性的规范与禁忌也不都是故作正派的假道学、强行压抑、或双重的标准。道德习俗之所以得以积累和流传，就在于遵守道德律令，反而能带来真正的长久、快乐的性满足，减少试验性的痛苦。<br />
<br />
<br />
现代自由社会不等于道德相对主义<br />
<br />
<br />
或许有人说，社群的衰落不是我所关心的事，个人权利至上，我有放纵的权利。很多人认为，只要符合“自愿、成人、隐蔽”三原则，只要不伤害他人，任何性行为不单在道德上都没问题，更是人权，任何程度的干涉（包括道德判断）都是侵犯人权。因此，现代社会就不能谴责换妻行为。<br />
<br />
持这种观点的人通常也认为，法律应该独立于道德和价值判断，道德完全是私人领域的事情，现代自由社会必然选择道德相对主义。<br />
<br />
其实对于这种观点，政治哲学家们已经有了很充分的讨论，这种粗线条的自由主义是一种典型庸俗自由主义观念。<br />
<br />
当代政治哲学界，以桑德尔、查尔斯・泰勒等人为代表的社群主义的兴起，就很集中地批判了这些观念。他们和众多学者们都看到，一个奉行道德相对主义的社会，如果在法律和公权力中完全排除了道德和价值判断，会导致自由的枯竭。社会中的小共同体的一些共同的道德和价值判断，对于保持自由社会的活力与方向，至关重要。<br />
<br />
如果整个社会只有几条法律框框是共同的规范，这样的社会的自由基础极其不稳定。法律和政治自由只是简单的骨架，并不能赋予自由社会以鲜活的生命。<br />
<br />
关于施密特的研究近年来在中国很热门，他就注意到，一个没有共同道德和价值判断、奉行极端自由主义的群体，反而极容易被极权所奴役。这就是他所说的，极端自由主义与极权只有一纸之隔。<br />
<br />
著名法学家伯尔曼也明确提出，自然法或者说道德法应该成为任何正义国度的政治、法律制度的基础。道德相对主义实际与虚无主义是一回事，这必然导致法律界的混乱。<br />
<br />
没有人否认自由是人最重要的价值之一，但人生的价值是多元的，若把任何一种价值绝对化，就会导致其他价值的忽略，把自由视作唯一价值也有同样后果。如家庭、爱的关系和尊重他人生命的要求，都意味着对个人自由的限制。<br />
<br />
有权利做某事并不代表做某事就一定是对的，批判一个人的行为也不等于剥夺这个人的政治自由。即使社会赋予我们权利作一件事，这件事仍可是不道德（如说谎、婚外情）、不值得鼓励甚至需要批判的（如懒惰、浪费、好赌）。 <br />
<br />
何种道德和价值是值得倡导的？尽管这是人类很难给出终极答案的问题，但这并不意味着道德和价值就只能是相对的。人类只在客观价值的引导下行驶自由，才能得到丰盛生命。</div>

							
							
							
							
