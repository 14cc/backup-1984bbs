<br />
这个说法可能让许多换偶人士感到兴奋，顿生一种“知音”遍天下之感，可事实果真如此吗？<br />
<br />
其实所谓的“1/4”完全是一个信口开河的数据。笔者查阅了该期《新闻报》，李银河先生提到的这一数据的是出自一个叫Federsex的组织，而“有关专家”，也是该组织的人。<br />
<br />
而这个Federsex组织，与ENTES是代表意大利换妻俱乐部的两大联盟，这些换妻俱乐部是靠卖门票维持的。根据ENTES的数据，在换妻俱乐部里夫妇与单身男性的比例几乎是一比一。另外，单身男性需要付出比夫妇更高的票价方可入场（150欧元：50欧元）。很明显，这实际上是一个性产业，有近一半是单身男性去“买春”而已。Federsex这种组织口里说出来的数据，显然当不得真，他们自己也说只是估计。把这个数据看作是换妻俱乐部自己的广告吹嘘，应该更为恰当。<br />
<br />
而在严肃的研究方面，社会学家理查德・简恩克斯在《换妻：文献回顾》一文中，也指出，换妻俱乐部的统计与严肃的调查数据差距极大，往往能相差十倍之遥，实在当不得真。<br />
