&nbsp; &nbsp; 自行脱党是指没有正当理由，自己脱离党组织。党章规定：&quot;党员如果没有正当理由，连续六个月不参加党的组织生活，或不交纳党费，或不做党所分配的工作，就被认为是自行脱党。&quot;这一规定表明，党员如果没有正当理由，连续六个月具有上述三种情况的任何一种，都算自行脱党。<br />
<br />
&nbsp; &nbsp; 处理党员自行脱党问题时，关键是看有没有&quot;正当理由&quot;和是否&quot;连续六个月&quot;这两个条件。不要把有客观原因的这类党员，一律不加分析地作自行脱党处理。党组织在发现党员有脱党行为时，应及时对其批评教育，帮助其改正错误，而不要等到六个月以后才去过问处理。如果本人不接受教育，坚持不改，则应按照党章规定处理。 <br />
<br />
&nbsp; &nbsp; 正确对待党员退党问题。党章规定：“党员有退党的自由。”因此，党员自愿要求并经党 组织按照有关规定办理手续后，可以退出党组织。党组织对党员的退党要求应按以下原则处理：<br />
<br />
(1)尊重党员退党权利，允许党员有退党的自由。中国共产党是中国工人阶级的先锋队。做一个共产党员不仅是自觉自愿的，而且是有条件的。党员如果改变了自己的信仰，或由于其他种种原因，不愿继续做一个共产党员而要求退党，党组织应予批准。<br />
<br />
(2)党员要求退党，应向党组织提出，不能通过大众传媒或以其他方式向社会公开。<br />
<br />
(3)党员要求退党，党组织首先要弄清党员提出退党的原因，然后区别处理。对于平时表现很好，只因有模糊认识或一时冲动提出退党，事后又主动撤回退党申请的，可以不作退党处理，但是应对其进行严肃的批评教育。是预备党员的，可延长其预备期。对于那些缺乏革命意志，对共产主义事业丧失信心，或者消极落后，甚至蜕化变质，或者个人主义膨胀，不愿接受党的监督和纪律约束的党员要求退党，应当及时作出批准退党的处理；对犯有严重错误以至有危害党的行为需要开除出党的，尽管本人提出退党，但不能按退党处理，而应按照党的纪律开除其党籍。<br />
<br />
(4)党员要求退党，党组织不要强行挽留，应尊重其意愿予以批准。<br />
<br />
(5)党员退党以后，党组织仍应从政治上关心帮助他们，不要歧视和排斥，要鼓励他们做个好公民。党组织也应从中吸取经验教训，切实加强对党员的教育和管理，努力解决党员的思想入党问题。<br />
