																											<div id="postmessage_409629" class="t_msgfont"><div class="quote"><h5>引用:</h5><blockquote>原帖由 <i>怪怪物</i> 于 2010-1-2 13:09 发表 <a href="https://1984bbs.com/redirect.php?goto=findpost&amp;pid=409618&amp;ptid=33254" target="_blank"><img src="https://1984bbs.com/images/common/back.gif" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /></a><br />
还有人记得文学锋么？ </blockquote></div>编者按 国防科学技术大学硕士研究生文学锋，在“三个代表”重要思想感召下重新入党，由此引发数百名青年学员积极向党组织靠拢的“文学锋现象”。<br />
<br />
　　解放军总政治部日前就《关于“文学锋现象”的调查报告》向全军发出通知，要求认真研究这一现象提供的启示，下大力营造浓厚的政治氛围，深入细致地做好用党的最新理论成果武装官兵的工作，进一步把全军的思想和意志凝聚在“三个代表”的伟大旗帜下，以崭 <br />
新的精神风貌和优异成绩迎接党的十六大召开。<br />
<br />
　　新华社北京6月9日电解放军总政治部调查组最近对国防科技大学99级硕士研究生文学锋在“三个代表”重要思想感召下重新入党一事进行调查，写出了《关于“文学锋现象”的调查报告》，现摘发如下：<br />
<br />
　　文学锋，1977年出生，1995年考入国防科技大学，现为科技哲学专业硕士研究生。他1997年被批准为中共预备党员后，感到“现实中的党与《共产党宣言》中描绘的党差距较大”，有些党员“没有党员的样子，党内还出了胡长清这样的严重腐败分子”……预备期满后，组织上几次找他谈话，他却不提出转正申请，并希望“在党外推动党的‘车轮’前进”。所在党支部根据党章规定，取消了他的预备党员资格。对此，文学锋很平静，他认为“假如一面做党员，一面又对党不满，这不仅于党的团结和事业不利，对个人而言也是一种人格的分裂。”<br />
<br />
　　江泽民同志“七一”讲话发表后，文学锋一连看了四遍，心灵受到巨大震撼。他感到，讲话围绕“建设一个什么样的党和怎样建设党”所作的系统阐述，澄清了自己的许多疑虑和困惑。“像怎样看待党内腐败，怎样看待党的先进性，怎样看待理想与现实”等，都能从讲话中找到答案。他认识到：“一个事物先进与否，不但要看它的现状，更要看它的发展方向和未来。‘三个代表’的党是先进而优秀的党，是大有希望的党，这样的党值得我追随。”于是，他接连向党组织递交了3份入党申请书。经过党组织严格考查，2001年12月，文学锋被重新批准为中共预备党员。<br />
<br />
　　“文学锋现象”是全军部队学习贯彻“三个代表”的一个典型范例，认真分析研究这一现象，将为深入学习贯彻“三个代表”提供许多深刻启示。<br />
<br />
　　一、必须把学习贯彻“三个代表”作为新时期军队思想政治建设的首要任务。“文学锋现象”的出现，原因是多方面的，从本质上讲，是“三个代表”重要思想在当代青年知识分子身上发挥巨大威力的结果。当代青年对党的正确认识，不单是靠情感体验，更多的是来源于对党的理论路线方针政策的认知和信服。而这种认同感一旦确定，就会有更坚实的基础。在今天，“三个代表”这一党的最新理论成果，最能帮助他们端正对党的认识，坚定跟党走的信念。<br />
<br />
　　基于这种认识，学校把“三个代表”作为教书育人之本，贯穿于教学科研之中，促成了像文学锋一样的一大批青年知识分子思想认识上的飞跃。文学锋在思想汇报中写道：“如果不是在这样一个环境中，我对‘三个代表’的学习不会这么自觉；如果不听学校的授课辅导，一些问题很难理解上去。”许多青年学员说，“三个代表”是顺应时代发展潮流的理论，学习后好似“百川归海”，又迎来了一个新的春天。<br />
<br />
　　理论只有彻底才能掌握群众，人们认同真理就会高举真理的旗帜。“文学锋现象”说明“三个代表”具有巨大的感召力，只要高扬起这面伟大旗帜，我军新时期的思想政治建设就抓住了根本，就会有强大生命力。<br />
<br />
　　二、必须着力用“三个代表”回答解决官兵关注的思想理论问题。文学锋是一个酷爱学习、勤于思考的青年。他把入党看作一种神圣的追求，总想从理论和实践上把党的性质宗旨和目标搞清楚。为此他读了不少经典著作，在心目中构建了一个理想“厦”，但经典著作与现实生活中的一些“对不上号”又令他沮丧。统一新时期青年的思想需要新的理论支撑，解决新形势下的思想困惑离不开党的创新理论的指导。学校以“七一”讲话为基本教材，把学员的思想问题作为理论研究的课题，用理论研究的成果教育、引导、帮助青年学员从“讲话”中寻找正确答案。<br />
<br />
　　真理是在与思想问题的碰撞中闪光的。“文学锋现象”说明，“三个代表”是回答解决各种现实思想问题的“总钥匙”。理论武装工作越触及现实思想问题，越能使科学理论深入人心；每解决一个现实思想问题，人们对真理的认同感就升华一层，理论武装工作就深入一步。<br />
<br />
　　三、必须下功夫把“三个代表”转化为官兵的世界观方法论。文学锋一度在思想认识上出现问题，说到底是观察思考问题的方法出了偏差。学校在组织他们学习“三个代表”过程中，没有满足于一般地学习了解基本内容和停留在简单地解开几个思想扣子，而是下功夫帮助他们掌握贯穿于“三个代表”中的科学的世界观方法论，深刻把握“七一”讲话蕴含的与时俱进、开拓创新的理论品质，解放思想、实事求是的科学态度，面向世界、面向未来的战略眼光。文学锋说，认识方法上的转变，是他学习“三个代表”的一大收获。<br />
<br />
　　“文学锋现象”表明，深入学习贯彻“三个代表”，不仅要理解和掌握它的基本观点，而且要努力掌握贯通其中的正确立场和科学方法，使之成为我们观察社会、指导工作、立身做人的准则。要破除凝固的思维定势和陈旧的认识方法，用创新精神学习党的创新理论，用发展的眼光看待变化的实际，使广大官兵在实践中进一步确立科学的世界观方法论。<br />
<br />
　　四、必须紧紧抓住践行“三个代表”这个关键环节。文学锋思想认识的提高，一靠真理的感召力，二靠实践的感染力。最近几年，我国改革开放和现代化建设事业蓬勃发展。同时，我们党大力改进作风，严厉惩治腐败，自身建设得到很大加强……学校充分运用这些生动事实开展教育，增强了大家的民族自豪感和自信心，看到了党践行“三个代表”的决心和行动。<br />
<br />
　　实践是学习贯彻“三个代表”的根本落脚点，也是党的创新理论成果最好的宣传语言。“文学锋现象”表明，群众对党和党的理论，更多的是从实践效果和党员的行动来认识的。如果每一个党组织、每一个党员都模范实践“三个代表”，我们党就有很强的凝聚力号召力，党的理论创新成果就更加容易被群众所接受。<br />
<br />
　　五、必须按照“三个代表”要求加强和改进思想政治工作。“文学锋现象”再次表明，推动“三个代表”的深入学习贯彻，有赖于强有力的思想政治工作；思想政治工作只有按照“三个代表”要求不断与时俱进，才能发挥强大威力。各级领导和思想政治教育工作者要以新的眼光和科学态度看待当代青年官兵，用创新的方法做好思想政治工作。青年知识分子知识面广，思维比较活跃，做好他们的工作，既要靠真理的力量、人格的力量，还要靠科学知识的力量。要努力适应教育对象文化水平日益提高的新形势，更新知识结构，丰富文化素养，特别要注意充实和掌握那些官兵关注的新知识新信息，掌握主动权，增强思想政治工作的质量和效果。</div>

							
							
							
							
