													<h2>大漠怀古：蒙古风的尴尬咏叹</h2>
						
						
																											<div id="postmessage_430886" class="t_msgfont">文章摘要: 今天，站在满目苍夷的国境线内，眺望着郁郁葱葱、十六年前就已经回归自由的蒙古国草原，足以让所有正直而清醒的中国人尴尬的羡叹和怀想。 <br />
&nbsp; &nbsp;<br />
<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;作者 : 曾节明<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;一，前言 <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;中华文明能够传承两千年而没有大的改变，主要是因为儒家的道统。儒家虽然能够长期有效地维持专制大一统的社会模式的稳定，却因为其轻视轻视科学技术、轻视宗教彼岸世界、敌视个性、自由、人权、民主的天性，越来越销蚀着中华民族的活力和进取心，儒家狭隘、内向、自我封闭的性质，阻隔着中华民族向外学习的胸襟和好奇心，儒家的意识形态实际上自南宋始，就已经开始成为整个中国社会的精神枷锁。儒家引领的道路不可能到达宪政民族，儒家实际上是中国社会向近现代转型的最大障碍。 <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;蒙古的征服由于彻底颠覆了儒家的道统，颠覆了中国儒家社会的纲常秩序，因此为中华文明充分吸纳西方及其他世界的进步因素敞开了精神的大门。 <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;元朝的八十九年，是中华文明等待伟大转机的八十九年，也是暮气沉沉、日趋衰朽的中华文明有条件实现伟大转型、重获青春的八十九年。 <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;但是取代元朝的朱元璋朱家政权，却把这扇被蒙古征服者无意中敞开了的大门重新关上。朱元璋以理学八股文取士的创举，在中华民族的精神上锁上了一把比先前任何时候的枷锁都更为沉重的牢固的大锁。中华历史的所有糟粕，由明朝的兴起而又全面沉渣泛起。 <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;二，大漠怀古 <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;在渐起秋风和斜阳下，审视沙化的混黄斑驳的内蒙大地、眺望朔北外蒙古那片湛蓝的天空、无垠的青青的草原和天边的白桦林，在细细的品位元朝的历史，我想任何兼有完备历史知识和公正心的汉族人都在那一瞬间会放下狭隘的民族自尊心，而为元朝的过早败亡无限惋惜。 <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;中华文明获得转机的机的可能，因元朝的过早败亡，早已化作天边的一缕晚霞，中华民族随即坠入了茫茫暗夜，至今仍未迎来光明。终元一朝，中华文明的步履和形态恰如马致远笔下的《天净沙.秋思》： <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;“枯藤老树昏鸦， <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;小桥流水人家， <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;古道西风瘦马， <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;夕阳西下， <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;断肠人在天涯” <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;终元一朝，西风劲吹，蒙古皇帝“离经叛道”，居然抛开摇头晃脑、一本正经的小儒、大儒、鸿儒，转过背去拥抱身着奇装异服、摆弄“奇技淫巧”的西方色目人，过着比“犬羊狄夷”还要“野蛮”荒唐的“西方腐朽生活”，这实在是令中国儒家知识分子和史官痛心疾首、嫉恨绵绵的事情！ <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;在儒家的引导下，南宋以后，中华文明已经衰相毕露，恰如“枯藤老树昏鸦”、“小桥流水人家”的光景虽然暂时还甜美和谐，但是文明之步履已如一匹瘦马在古道上蹒跚行进。在阵阵的西风中已经能够凸现尴尬。知耻方能勇，看到差距才可能奋起直追，只是中华文明的步履还来不及调整，短暂的元朝就像漠北秋日午后的太阳，转瞬之间已无精打采地斜挂在天际，蒙古人周游世界累了，可以又回到草原，钻进蒙古包睡个好觉，中华民族却从此沉沦在更加漆黑的精神黑夜当中。 <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;在东亚民族当中，蒙古族是一个最奇特的民族：它是一个黄种人民族，一个土生土长的东方民族，它的目光却始终朝向西方，无论是征服的屠刀还是学习的兴趣，都始终指向西方……中亚、西亚、北非...直至万里之遥的中欧，只有在实在鞭长莫及之后，蒙古人的“上帝之鞭”才指向南方的华夏。 <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;同样是黄种人，日本人、朝鲜人、越南人如饥渴似渴的接受了中国的儒家文化；鲜卑人、契丹人、女真人轻轻松松地完全融入中华、满州人屠戮汉人之武功，不可谓不超绝盖世，却最迫不及待地浸入了儒家腐朽经传的墨汁中，整个民族化为乌有；蒙古人却倔强的始终面朝万里之遥的西方，流露着对近在身边的、信奉儒家的诺大南方邻居的漠视，尽管邻居浑身珠光宝气；尽管自己成了邻居的主人。 <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;“一代天骄”成吉思汗毫不掩饰地表露出对儒家的轻蔑，他宁可满怀热情地接纳汉族道士丘处机，也没有耐心多听一句耶律楚材的儒家说教，尽管耶律楚材是汉化的契丹人。同样面对的是被征服者，忽必烈等蒙古君主宁可重用西方“色目人”，却一再疏远翘首以望一官半职，惯以愚忠为能事的汉族儒家知识分子。 <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;可见，元朝，是多么令中国儒家羞愤交加、恨之入骨的一个王朝！无怪乎，大明朝智囊、大儒刘伯温数月间便草草修成了一部《元史》，儒家羞愤之情，尽现草率之中。羞愤之下，在中国的史书中国的儒家史书当中，元朝是禽兽虏朝，蒙古人当然是禽兽不如了。 <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;的确，蒙古人是粗陋的，粗陋的迟迟没有自己的文字；但蒙古人又是精致的，精致得不像后来满州人那样死抱“弓马取天下”的“根本”，可以及时地大量引进和学习西方的火器和舰队，大宋的襄阳城就是失陷在蒙古军的“回回炮”的炮火中。在蒙古人面前，华夏的兵器也没有了优势，“回回炮”的炮声，始终是那些坚持中国文化优越论的人挥之不去的梦魇；蒙古人的精致，精致得有那样独到和敏锐的文化鉴别力：早在十三世纪就确立了西向的道路。究竟是什么，让蒙古人始终拒绝选择近在身边儒家道路呢？这个千古疑问，就是蒙古族特有的、永恒的魅力。 <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;蒙古人倔强，倔强得入主华夏八十九年而不汉化，最终全身尔退，返归漠北摇篮，从哪里来、回哪里去，蒙古族的民族特征丝毫没有丢失，至今在蒙古草原上，拌着马头琴琴音的蒙古大汉的歌喉，一如八百年前那样粗阔和嘹亮。至今，独立的蒙古国依然屹立在蒙古高原上，一如八百年前，这实在让那些死抱大一统观念的中国人羞愤难当。 <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;蒙古人倔强，倔强的面朝西方，错了也不回头，以致达到了虔诚的境界。蒙古人历史上倔强地抵制儒家礼制，坚持着类西方的分封制。面对同样的昔日的被征服者，蒙古人宁愿亲近俄国人，也不愿亲近中国人。历史证明蒙古人是对的：俄国人再咄咄逼人，却愿意成就蒙古国的独立；中国人再温良礼让，却死也容不得蒙古人的独立。蒙古人倔强的跟随着俄国人，倔强得步入苏维埃歧途；蒙古人仍然倔强地跟随着俄国人，反而在亚洲的共产国家中，第一个革除了共产党专制的毒瘤。 <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;这究竟是什么原因？中华文化难道要比蒙古文化低吗？我似乎明白了什么，却又刹那间不能思索、不能自己，只有一个人悲切嘶嚎、仰头久久问青天...... <br />
