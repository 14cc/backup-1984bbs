													<h2>写给挺儒的那些人们</h2>
						
						
																											<div id="postmessage_472461" class="t_msgfont">[原创]写给挺儒的那些人们<br />
文章提交者：鬼首天龙 加帖在 猫眼看人 【凯迪网络】 <a href="http://www.kdnet.net" target="_blank">http://www.kdnet.net</a><br />
<br />
生活中有几个朋友，经常在一起喝酒扯蛋，彼此是怎么回事，可谓知己知彼。中国人聚在一起，好谈论跟自己无关紧要的事情，家事国事天下事，事事关心，不知道中国人好这口的，隔着一层窗户纸在外面偷听，以为是联合国常任理事国的几位大佬在讨论关于世界和平的问题呢。没办法，谁让咱中国人素质高呢！<br />
<br />
不久前，电影《阿凡达》与《孔子》先后上映，一时间，脸上长嘴的中国人，几乎都在热议这两部影片。有关部门在用美国大片赚足了咱人民的币之后，企图再拿孔老二把永远不明真相的群众忽悠进电影院，不仅想赚屁民的钱，还想赚屁民的泪。孔老二的扮演者周润发对记者说：看《孔子》不哭就不是人！我觉得周润发被孔老二这傻给逼住了，他不仅瞧低了中国人民的智力水准，也错误地估计了炎黄子孙的坚强程度。<br />
<br />
当我们所居住房屋被暴力拆迁、为谋生的摊位被任意砸毁、年幼的女儿被权力猥亵、鲜活的生命被合法自杀的时候，面对这一切应该流泪的情与景，我们都不曾哭过，你周润发穿上古装，连人话都不会说了，之乎者也一番，我们就得痛哭流涕，靠！谁把谁当外星人了啊？<br />
<br />
留神了一下《孔子》的票房，似乎行情不好。虽然广电总局有行政命令，让《阿凡达》强行下线，给《孔子》一条活路，好像也未见起死回生，反而落下笑柄。广电总局的大佬们本想在屁民面前充一把爷，没想到被孙子了，因为哥抵制的不是《孔子》，哥是抵制《孔子》背后的孙子。<br />
<br />
跟朋友聊天，东拉西扯之后，随口问上一嘴：“看《孔子》了吗？”我惊奇的发现，得到的答案几乎是同一色的，没有人去看过《孔子》。这里面不乏那些素来对儒学情有独钟，视老二为天人的朋友，这实在有点出乎我的意料。我以为这些人平素里对老二长嘴孔圣人闭嘴老夫子的恭维着，自诩为儒门弟子，在此《孔子》上映之际，怎么着也得慷慨解囊，用实际行动表示自己对孔儒的支持。万没料到，反儒的哥们抵制《孔子》背后的孙子，而《孔子》背后的孙子们抵制的却是《孔子》。究其一毛不拔的原因，他们认为胡导的《孔子》不是真正的孔子，《孔子》表达的儒不是真正的儒，我觉得这就是纯粹的扯蛋了。<br />
<br />
当年于丹在百家讲坛讲解《论语》，反儒的朋友给予迎头抨击，这不足为奇。奇怪的是挺儒的人们对于丹也不屑一顾，很多大学里的教授、学者、专家们都纷纷跳出来，指责于丹曲解《论语》，令人感觉好笑，我不知道谁对《论语》有绝对的诠释权。我以为他们不过是一地鸡毛。<br />
<br />
今天的胡玫遭遇到了与当年的于丹同样的情景，在反儒的人们对《孔子》的一片抵制之声中，不见挺儒的人们伸出援救之手，眼巴巴地看着投资巨大的《孔子》在《阿凡达》面前一败涂地，除了幸灾乐祸，幸灾乐祸之余，我想表达一点悲伤之意和同情之心，给孔子和《孔子》背后的孙子们。但是，无话可说。<br />
<br />
与挺儒的朋友交流过，在他们的眼中，于丹的《论语》不好，胡玫的《孔子》不行，清代的儒不是真儒，明朝的儒也是赝品，因为清主非我族类，明朝的儒国破家亡。更进一步地讲，连汉武帝独尊的儒术也是假冒伪劣，我真的被糊涂了，我不知道这些人力挺的儒究竟是什么，能不能在给我一个明确的答案之前，你们先统一一下思想和认识，再出来现眼不迟。<br />
<br />
我还问过挺儒的朋友，在二十一世纪的今天，你们力挺孔老二这具千年古尸，目的何在？如果你们只告诉我孔老二的几本书写得不错，读过之后可以使人变得高尚起来，纯粹起来，能够脱离低级趣味。翻遍中国历史，饱读诗书的儒们成千上万，我也没发现有几个道德高尚、情操纯粹、真正的脱离了低级趣味的人来，满嘴仁义道德，一肚子男盗女娼的混蛋倒是一群一群的，在历史的舞台上轮番粉墨登场，祸国殃民，这种特色的中国国情，如何解释？<br />
<br />
我曾直截了当地问过挺儒的人们，是否需要效法当年汉武，重新独尊儒术，把孔老二及其儒家学说作为治国的根本所在？你们如此卖力的坚挺着孔家铺子，有没有政治上的诉求？挺儒的人们吱吱唔唔，顾左右而言他，没有人正面回答我的问题，或许是不好意思。没关系，我善解人意。<br />
<br />
我以为于今天挺儒的人们在心理上是患有疾病的人，他们的大脑结构异于常人，因为他们的思维不合逻辑，矛盾百出，不能自圆其说。这也难怪他们，孔老二就是一个没有哲学思辨能力人。<br />
<br />
说孔老二是个教育家，我觉得还靠谱，能总结出“温故而知新”这么高深的教育经验，真的不容易。说孔老二是个思想家，我觉得不恰当，思想家是个含混模糊的概念，每个人都有思想，如果说坐在家里天天思想就可以被称为思想家的话，那我也是其中一个。说孔老二是个哲学家，那是挺儒的人们往老二脸上贴金，他实在不配这一称号。<br />
<br />
孔老二毕生追求的政治理想，其所致力于的 “克己复礼”，这个礼是周礼，也不是他孔老二发明创造的，他只不过是力图恢复，想让所有的人在等级森严的礼制下安分守己的活着，但是，没有一个乱世之中的国君听从他这一套，孔老二周游列国，惶惶如丧家之犬，够狼狈地，这是他自己说的。<br />
<br />
西哲黑格尔曾评价过孔老二，说在孔子那里，“思辨的哲学是一点也没有的，只有一些善良的、老练的、道德的教训，从里面我们不能获得什么特殊的东西”。黑格尔认为孔子所讲的只是道德常识，而“这种常识道德我们在哪里都找得到，在哪一个民族都找得到，可能还要好些”。在这位哲学家的眼里，儒家经典《论语》也没有多大价值，从内容到形式， “毫无出色之点”。黑格尔判断：“孔子的哲学就是国家哲学”。他认为中国的道德学说是为专制的政治服务的。最让人感到难堪的是黑格尔说：“为了保持孔子的名声，假使他的书从来不曾有过翻译，那倒是更好的事。”看到最后这句话，代表那些挺儒的人们脸红一下。<br />
<br />
黑格尔对孔子的评价，或许不应该作为绝对的权威定论，但是，一个在哲学史上有着巨大影响力的哲人的眼光，自有独到之处。<br />
<br />
看到过的反儒的和挺儒的争论太多了，我知道再简单的道理讲给挺儒的人们听，也是对牛弹琴，无济于事，我十分的怀疑他们在出生的时候，脑袋是否被产钳夹坏了，或者在小时候被傻子亲过。<br />
<br />
算了，不说了。</div>

							
							
							
							
