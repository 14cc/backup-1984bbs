&nbsp; &nbsp; 在这历史时刻，中共上海市委书记江泽民连夜赶赴北京，出任中共中央总书记。到京成为中国最高领袖的江泽民，对首都街头，特别是天安门广场戒严的愁云，百思不解，耿耿于怀，无法找出国运动荡之源头。<br />
