&nbsp; &nbsp; 高人们抵达首都后，明确了这次应邀到京的任务。他们深居钓鱼台国宾馆，各出绝活，各显神通，各露身手，一人有一招，若无旁人。有的朝起暮归，串街走巷，端罗盘，估方位；有的深居简出，盘膝作法，通神灵，念咒语；有的运用数理推算国运，有的以格局衡量气脉。真是国家兴亡，匹夫有责。只有李建军端着北京市地图，溜达在天安门广场，在大旗杆下沉思、发楞。日复一日，在天安门广场的旗杆下，来回走了几天，目光凝聚在飘扬的五星红旗。他想起国旗的定义，查出了共和国第一旗定位的偏差，找到了扭转乾坤的良策。<br />
