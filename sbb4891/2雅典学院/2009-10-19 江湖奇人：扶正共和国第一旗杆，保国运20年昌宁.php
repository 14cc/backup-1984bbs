&nbsp; &nbsp;&nbsp;&nbsp;李建军在天安门广场实地勘察中，发现了天安门广场大旗杆定位中的三大问题，第一，由于毛主席纪念堂的设立，使正处中轴线上的天安门广场旗杆南北距离的比例失调。当时确定天安门广场旗杆的定位，是根据建国初，天安门广场两侧历史博物馆、人民大会堂和南侧人民英雄纪念碑竣工时的格局所定的。并无毛泽东纪念堂建造的考量，所以毛泽东纪念堂建成后，形成了南北距离失调，不论是空间格局还是地面比例，都是南重北轻、朱旺玄弱之势，所以国旗的定点，必须重新确认定位。第二，该旗杆设立于五十年代，近40个春夏秋冬中，整个中华大地和首都北京都发生了巨大变化，而一成不变的旗杆高度，已经压抑到社会高速发展的运脉。调高现有旗杆的高度，势在必行。第三，旗杆四周的汉白玉围杆，呈口字型封闭状态，使国家运程陷入封闭不通之格局，并且无法展示升旗的中国人民解放军仪仗队升旗的雄姿。<br />
