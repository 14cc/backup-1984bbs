													<h2>抓好共产党员的无神论教育</h2>
						
						
																											<div id="postmessage_291046" class="t_msgfont"><a href="http://hengjie.luqiaodj.gov.cn/read.asp?id=294" target="_blank">http://hengjie.luqiaodj.gov.cn/read.asp?id=294</a><br />
抓好共产党员的无神论教育――中共中央党校民族宗教理论室主任龚学增教授访谈录<br />
<br />
<br />
我国正处于并将长期处于社会主义初级阶段，马克思主义唯物论、无神论与唯心论、有神论的斗争，也将是一个长期的过程。全面建设小康社会必须要有强大的精神支柱，这个精神支柱的基点就是马克思主义的科学世界观、人生观、价值观。要使马克思主义的唯物论和无神论的教育、宣传取得好的成效，关键是要切实抓好共产党员的无神论教育。记者：我们党向来崇尚科学、反对迷信，共产党人应该都是彻底的唯物主义者，为什么当前要特别强调加强对共产党员的无神论教育？<br />
<br />
龚学增：中国共产党是以马克思主义武装起来的工人阶级先锋队，是以辩证唯物主义和历史唯物主义的科学世界观作为理论基础的，党的这一性质和世界观基础要求共产党员必须成为真正的马克思主义唯物论者、无神论者。<br />
<br />
恩格斯说，马克思主义政党有一个优点，就是以科学的世界观作为理论的基础。在新的历史时期，江泽民同志多次要求全党特别是领导干部要不怕鬼，不信邪，坚持真理，维护党的原则，旗帜鲜明地同各种错误思想、不良倾向和邪恶势力作斗争。指出共产党员是无神论者，共产党员的世界观应该是马克思主义的世界观。共产党员不但不能信仰宗教，而且要向人民群众宣传无神论，宣传科学的世界观。<br />
<br />
从一定意义上说，中国共产党的历史，也是崇尚科学、破除迷信的历史。对于愚昧迷信和伪科学以及邪教的出现，我们也应反思党内的原因。有神论在党内的影响，既有旧的有神论的影响，又有新的有神论的影响。所谓旧的有神论的影响，即传统封建迷信思想和传统宗教有神论的影响，主要表现为少数党员干部信神、信风水、信宿命论，求签问卜、修坟建庙，甚至把迷信手段引入了机关场所和公务活动之中。所谓新的有神论对党内的影响，集中表现为极少数共产党员参与邪教的一些活动。<br />
<br />
可以看出，这些都直接损害到党的肌体。党内少数人在对科学与迷信、无神论与有神论问题上一定程度的思想混乱，会助长社会上的愚昧行为，动摇我们的理想信念，因此必须高度重视，并把抓好共产党员的无神论教育作为首要任务。记者：加强对共产党员的无神论教育，积极开展对广大人民群众的无神论宣传，是否与我国的宗教信仰自由政策相矛盾？龚学增：进行无神论教育、宣传活动与国家保障公民的宗教信仰自由并不矛盾。我们党制定和实行宗教信仰自由政策，是遵循宗教发展的客观规律。共产党人是无神论者，但知道建立在有神论基础上的宗教是人类社会发展到一定阶段的历史现象，有其产生、发展和消亡的客观规律。宗教信仰问题是思想领域的问题，只能说服教育，不能强迫命令；只能正面引导，不能压制打击；只能采取民主的方法，不能简单粗暴。群众对宗教信仰的态度和选择，是他们个人的事情，应予以尊重，不能加以干涉。信教群众和不信教的群众在思想信仰上的差异是次要的，他们在经济政治上的根本利益是一致的。因此，只有真正贯彻执行宗教信仰自由的政策，才能充分调动信教群众在我国社会主义建设和改革中的积极性。<br />
<br />
马克思主义唯物论和无神论的宣传教育本质上和科学普及工作是一致的，但又有自身的一些特点。马克思主义无神论实质上就是辩证唯物主义和历史唯物主义的世界观和方法论应用于对有神论研究和批判的必然结论。具体来说，它是以辩证唯物主义和历史唯物主义的世界观和方法论作为哲学基础而形成的，科学批判和彻底否定有神论宣扬的神的存在并主宰一切、灵魂不死、天堂地狱、来世幸福及其他鬼神迷信、各种巫术的理论。因此，马克思主义无神论更为重要的任务是始终紧紧依靠马克思主义哲学和现代自然科学成果，科学地说明有神论是怎样产生的，它的发展规律是怎样的，为什么说科学的无神论会最终战胜有神论，从而帮助人们逐步树立科学的世界观。<br />
<br />
既实行宗教信仰自由政策，又强调对党员和群众特别是广大青少年进行无神论教育，这就要把握好反对迷信、宣传无神论与保障宗教信仰自由的界限。在开展科学无神论宣传教育的过程中，不能违背宗教政策。记者：现阶段，如何进一步加强对共产党员的无神论教育？<br />
<br />
龚学增：首先，要高度重视在党内开展唯物论、无神论的宣传教育。党和国家的工作千头万绪，中心是搞好经济建设。党内的唯物论、无神论教育工作虽然是一项局部的具体工作，但要把开展这一教育上升到关系党能否坚持马克思主义思想路线的大问题，关系我们共产党人确立什么样的精神支柱的大问题。对这一涉及共产党人的根本信仰，涉及全国人民团结奋斗的思想基础，涉及党和国家前途命运的重大问题，各级党委和每一个共产党员特别是领导干部都必须立场坚定、旗帜鲜明，深刻认识在党内进行唯物论无神论教育的重要性。<br />
<br />
其次，要把抓好无神论教育作为党的思想理论宣传工作的重要内容。用马克思主义立场、观点、方法对宗教问题进行科学研究，用马克思主义哲学批判唯心论（包括有神论），向人民群众特别是广大青少年进行辩证唯物论和历史唯物论的科学世界观（包括无神论）的教育，是党在思想战线上的重要任务之一。建设一支用马克思主义武装起来的宗教理论研究队伍，是党的理论队伍建设的重要方面。今后，加大无神论宣传教育的力度，首先要面向党内。要加强对党员特别是领导干部马克思主义哲学的教育，在党内旗帜鲜明地、理直气壮地高扬马克思主义唯物论和无神论的旗帜。明确共产党员要自觉抵制有神论的影响，树立科学的世界观、人生观和价值观。如果党内搞好了这方面的教育，就能在群众中起到良好的示范作用，就能更有效地领导和组织群众开展弘扬科学、破除迷信的工作。</div>

							
							
							
							
