																											<div id="postmessage_289920" class="t_msgfont">旷世奇才李建军<br />
<br />
<br />
<br />
<br />
李建军<br />
中国人民解放军武装警察技术学院教师<br />
运用特异功能，以肉眼探测出秦陵秦始皇尸体保存完好，<br />
并用超静意念发现了震惊世界的秦朝将军俑坑。<br />
<br />
李建军<br />
中华人民共和国国务院总理赵紫阳特别护理<br />
探索水晶之妙用，确保总理健康。<br />
<br />
李建军<br />
深圳大学人体工程学系副教授<br />
勘察天安门广场风水，扶正人民共和国“第一国旗”。<br />
<br />
李建军<br />
人体工程学开派大师<br />
在中共大举军事演习，台湾一片恐怖的闰八月，独家开讲：<br />
“海峡无战事！<br />
<br />
<br />
<br />
李建军亦名李森，祖籍中国陕西省米脂县人氏，五十年代中出生在古都西安，为关中名儒赵润生（注：赵祥林，字润生，国学大师，玄学家。【1892年5月1日至1980年3月3日】）之外孙。<br />
<br />
李建军家学渊源，天赋特异功能，具神力而通达阴阳十方，悟天命而博涉古今百科。他遍罗儒学、道教、显密佛学及传统易学中易经八卦、阴阳五行、河洛理数、四柱推命、六壬神课和相学、风水、观星术，与现代医学、气象学、地理学、物理学、人际关系学、心理学、美学等学科为综合体系。<br />
<br />
八十年代出任中国深圳大学人体工程学系副教授，他自成体系，独创学派，成为当代人体工程学之东方学派的开派大师。也是唯一以命理、生理、地理咨询而出入于两岸三地最高政权机构的旷世奇才。<br />
<br />
李大师的足迹、遍布中外各地，在梵帝冈为教宗调理皇体，在华盛顿看白宫风水，在开罗测金字塔子午线，在汉城揭总统府地下毒咒；他广交政、商、学界名流，为李鹏总理配带“齐眉镜”，为王永庆的六轻工程奠基；与菲律宾总统拉莫斯谈“齐身治国”，同法籍好莱坞巨星尚格云顿解“缠身隐患”；重学养、勤实践，获颁“宗教金像奖”，荣赝马来西亚艺术学院客座教授。<br />
<br />
慧心法眼&nbsp;&nbsp;国宝重辉<br />
<br />
民国元年夏天，中国陕西已经历了长达七十五天的干旱。<br />
<br />
炎炎烈日，只有微风撩起阵阵黄土，酷署难熬。临潼县晏寨村农民纷纷打井取水，抱着一丝生机，在挣扎。穷山恶水多硬汗，一脸愁容的老和带着七岁的幼子万春，艰辛地在家门口挖井，井挖成了，却见不到半滴水，“水哇！！！”叫天天不应，挖地地无水，再往下挖，老和意外地挖出一个陶质人头。<br />
<br />
村民们围在老和家门口，围观陶质人头，惊奇万分，纷纷揣摩这只陶质人头的来历和在挖井中出现的意义，及与大旱的关系。善良本份的农民们明白了，陶人头是上天派来视察旱情的使者，是灵验的神像。村民们立即将陶制的人头高高供奉起来求拜，希望他们的虔诚能感动上天，从枯井中涌出清凉的甘泉。全村上下、男女老少，在老和的领下，磕头求拜。拜了几天，枯井还是哪个枯井，黄土还是哪个黄土，老和看着枯黄的庄稼，呛着满眼的老泪，绝望地敲碎了没有一点仁慈的陶头，打破了全村人的最后希望。<br />
<br />
时隔六十个春秋，陕西依然是热风伴黄土，晏寨公社西杨生产队社员们，在村南百米处挖掘灌溉农田用的机井时，在4米深处，又发现了陶质人头，接着又挖到许多陶质的身体、人手、脚掌，其形状大小与真人相仿。这一奇异的发现，震惊了国家文物管理机关。时后，在国家文物局的领导下，由陕西省博物馆等单位成立了考古挖掘队伍，及时找到了在民国元年打破陶头老和的独子和万春，在这位近七十的老人带领下，来到了六十年前他父亲挖井的地方试掘，终于揭开了2000多年前秦国兵团的神秘的雄姿----秦始皇兵马俑坑。<br />
<br />
兵马俑坑位于皇陵正东1000米处，规模庞大，占地12600平方米，整齐有序的兵马俑和战车，排列成秦始皇带入地下的一支御林军。<br />
<br />
兵马俑的出土，震撼了全球。展现了中国古代文化艺术的辉煌璀璨，被誉为“世界八大奇迹”。但是秦始皇墓依然是一个迷，史籍也没有确切的记载，唯有《汉旧议》曾提到：“始皇使丞相李斯将天下刑人徒隶七十二万作陵……奏之曰:丞相臣斯昧死言:臣所将隶徒治骊山者，已深至极，凿之不入，烧之不然，叩之空空，如下天状。”千年来，不断有秦始皇墓已被盗取一说。1984年，为了查清秦始皇墓真相，国家文管部门与秦岭博物馆联同陕西省文物管理委员会、陕西日报社，请来了中国武装警察技术学院年轻教师李建军，要求他发挥特异功能，用肉眼测视秦始皇墓，揭开秦始皇墓的迷团。<br />
<br />
月光洒落在八百里秦川，一片寂静，武警教师李建军随秦陵博物馆刘副馆长来到兵马俑坑，进入了绝不对外开放的地下坑道，进行感应意念运作经李建军的特异功能目测，秦始皇墓内的秦始皇尸棺，位于兵马俑西北10公里处，墓穴宽敞，内有大面积银光闪射反应，尸体完整，唯秦始皇尸体的头部银光强烈。身处一旁的刘副馆长，听到李建军的报告后，由禁若寒蝉到错愕称绝，李建军的目测验证了秦始皇墓穴的正确位置，同时也证实了经地质勘探认定的秦始皇棺木是漂置在大量水银中的结论。在李建军报告的七年后，新华通讯社的一则消息完全证实了李建军论点的正确：“探索秦始皇陵园奥秘取得突破性进展，着是陕西地质工作者在考古部门协助下，经过三年勘查、研究的结果。勘查表明，目前秦陵地宫基本保存完好，未遭严重破坏和盗掘。……位于西安东三十公里、骊山北麓的秦始皇陵是世界规模最大、结构奇特、内涵丰富的帝王陵园之一。在陕西省文物局的支持下，1989年陕西省地矿局成立科研课题组，开始对秦陵进行勘察和综合研究。科研课题组通过地下水化学分析和对比研究，确认地宫中有着丰富的水银和金属器物。”<br />
<br />
李建军在特异功能地下目测中，在其超静意念感悟时，突然发现在兵马俑坑口左侧三公里处，又有一个大型俑坑，其陶俑的数量、阵势的规模与兵马俑坑相仿，只是这些陶俑的服饰装束、姿态与兵马俑略有异样。<br />
<br />
根据李建军的描述，经过秦陵博物馆的试验挖掘，确认为将军俑。出土将军俑的地点，将军俑的服饰装束完全与李建军超静意念目测后叙说的一致。这一重大发现经报中央批准，开掘了再次震撼中外的将军俑坑。<br />
<br />
<br />
大内高手&nbsp;&nbsp;杏林春风<br />
<br />
1985年春天。<br />
中华人民共和国总理赵紫阳在第六届全国人民代表大会第三次会议中，发表了《当前的经济形式和经济体制改革》的重要报告。<br />
<br />
当天会议之后，赵总理回到中南海西小院。不断地眨眼，露出了不安烦躁这一症状立刻引起了中央办公厅特别护理李建军的警觉，作为中央最高领导人的保健顾问，他有责任保证首长的健康。这一年，总理正面临着史无前例的经济改革实施，虽说他已年届66岁，但日理万机，忙于国事，由于不间断地工作，而引发了眼睛疼痛。六届人大三次会议后，保健医生为赵总理的眼睛作了全面的检查，却查不出病因，专科医生也认为并无大碍。<br />
<br />
选用了一些眼药，要求总理多注意休息，这些措施并没有解除总理的眼疾，眼痛伴着头痛，无时不在。为了保证总理在六月出访英国、联邦德国和荷兰三国的行程，李建军大胆提出了立即为赵总理配带天然茶色水晶眼镜的方案。<br />
<br />
中央机关根据李建军的提议，责令地方有关部门从东海县开采了六棱形茶色水晶原石，连日送往苏州切割加工，研磨成镜片，李建军也专程由首都赶到苏州，提出专业要求，及时制成三付茶晶墨镜。<br />
<br />
赵紫阳总理带上经李建军督造并发功加持的水晶墨镜后，眼疾立刻消除，感到头脑特别清醒，就连多年的神经痛感也消除了，连连称赞特别护理李建军高人高招，杏林春风。并从三付水晶墨镜中挑了一付，送给李建军。<br />
<br />
扶定国旗&nbsp;&nbsp;扭转乾坤<br />
<br />
八十年代的最后一个年头，中国北京发生的“六・四”天安门事件，顿时成为全世界关注的焦点。<br />
<br />
在这历史时刻，中共上海市委书记江泽民连夜赶赴北京，出任中共中央总书记。到京成为中国最高领袖的江泽民，对首都街头，特别是天安门广场戒严的愁云，百思不解，耿耿于怀。无法理出国运动荡之源头。针对时局，江总书记在政治体制、经济体制上作出一系列的改革措施，以体现反腐败、讲民主的新风。同时也希望在其它领域查出国家动荡、政权不稳、民怨高涨的原因。中共中央直属机关根据江总书记的指示，秘密地从全国各地请来数十位精通风水地理、玄学术数和特异功能的高人。<br />
<br />
这群应邀前来北京的高人中，不乏专家学者、著作等身的名儒宿彦、其中最年轻的就是深圳大学人体工程系副教授李建军先生。<br />
<br />
高人们抵达首都后，明确了这次应邀到京的任务。他们深居钓鱼台国宾馆，各出绝活，各显神通，各露身手，一人有一招，若无旁人。有的朝起暮归，串街走巷，端罗盘，估方位；有的深居简出，盘膝作法，通神灵，念咒语；有的运用数理推算国运，有的以格局衡量气脉。真是国家兴亡，匹夫有责。只有李建军端着北京市地图，溜达在天安门广场，在大旗杆下沉思、发楞。日复一日，在天安门广场的旗杆下，来回走了几天，目光凝聚在飘扬的五星红旗。他想起国旗的定义，查出了共和国第一旗定位的偏差，找到了扭转乾坤的良策。<br />
<br />
一周后，在中直机关的主持下，召开了论证会议。高人们拿出了各自的报告，各持己见，相互较劲。有的提出：中南海与北海相连的水域被铁栏杆相隔，脉气不通，造成“龙困浅滩”之局，使政权不稳。有的提出：毛泽东纪念堂压到龙脉，断了国运。也有的提出：人民大会堂西北侧的一大坑，引起燥动，造成动乱。而李建军教授以事实为依据，以科学数据为凭证，发表了“开放、通达、国泰、民安”独家之说，将问题之源直指天安门广场的旗杆定位。<br />
<br />
李建军在天安门广场实地勘察中，发现了天安门广场大旗杆定位中的三大问题，第一，由于毛主席纪念堂的设立，使正处中轴线上的天安门广场旗杆南北距离的比例失调。当时确定天安门广场旗杆的定位，是根据建国初，天安门广场两侧历史博物馆、人民大会堂和南侧人民英雄纪念碑竣工时的格局所定的。并无毛泽东纪念堂建造的考量，所以毛泽东纪念堂建成后，形成了南北距离失调，不论是空间格局还是地面比例，都是南重北轻、朱旺玄弱之势，所以国旗的定点，必须重新确认定位。第二，该旗杆设立于五十年代，近40个春夏秋冬中，整个中华大地和首都北京都发生了巨大变化，而一成不变的旗杆高度，已经压抑到社会高速发展的运脉。调高现有旗杆的高度，势在必行。第三，旗杆四周的汉白玉围杆，呈口字型封闭状态，使国家运程陷入封闭不通之格局，并且无法展示升旗的中国人民解放军仪仗队升旗的雄姿。<br />
<br />
一言道破天机，李建军教授的报告，得到了中央的完全采纳，根据李建军教授为天安门广场国旗定位、旗杆高度及地基围栏的方案，中央为九百六十万平方公里上的共和国“第一国旗”重新圈定了发祥之地。<br />
<br />
道破天机&nbsp;&nbsp;台海平安<br />
<br />
1994年，台海风云突变。<br />
<br />
台湾各大书局推出了署名郑浪平的《一九九五・闰八月》一书。该书发行后，即刻以25万册的惊人销量，使海峡两岸的关系罩上一层浓厚的乌云，书中以中国传统玄学预测了北京将在1995年的农历闰八月武装犯台，此书在两岸造成巨大的旋风，导致了台湾社会人心不安、经济滑坡，台湾民众犹如惊弓之鸟，仿佛已经到了大战的前夜。美国之音指出：中共武装犯台之说，造成台湾民众高度恐慌。日本朝日电视台说：“此书在台湾国会引起热烈讨论，立法委员争相咨询行政首长。”德国新闻周刊也报导了因为台湾民众高度恐慌中共武力，再度引发了台湾移民潮达到高潮。<br />
<br />
1996年初，台湾民众总算熬过了惊心肉跳的闰八月，又因为首届台湾民选总统的展开，触发了北京当局以警告台独为理由，在于台湾相邻的海域大举军事演习，以“文攻武吓”的策略，决不让李登辉登上总统宝座，强大的武力威胁达到了高潮，飞弹横扫台海。中共即将武装犯台，以擦枪走火酿成血染宝岛之说，足使台湾民众震荡失魄，天昏地暗，就在这危机时刻，台湾中国电视台《2100全民开讲》节目请来了定居台湾的人体工程学开派大师李建军先生。创下了这个节目自开播以来的收视力最高记录。<br />
<br />
李建军教授以人体工程学理论详细破解了台湾现有的格局和运势，并以传统星象学阐述了天体影响时事的根据，独家识破天机：“海峡无战事！”同时也肯定性地预告了北京当局大举武装演习，不构成对台湾动武的条件，中共的飞弹，只是凝聚台湾民众的情结，成为李登辉先生登台当选总统的助力，李登辉先生将以高票当选，出任第一任台湾民选总统。时隔一个月，一切灵验了李建军的大师高论。<br />
