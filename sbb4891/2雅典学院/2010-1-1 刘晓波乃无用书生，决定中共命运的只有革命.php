																											<div id="postmessage_409542" class="t_msgfont">关羽你的思想一点都不新鲜啦――请看看陀斯妥耶夫斯基的《群魔》吧，里边主角阴谋政变暗杀什么都有，可民粹的土壤里最终结出的是颗什么样的果实呢？是人类历史上最邪恶的政权。<br />
<br />
以暴力、杀人为手段，为惟一手段的革命，建立起来的肯定是一个坏政权，你凭啥呼吁大家付出血流成河的代价然后相信这样带来的是一个扬眉吐气的新时代？面对暴力，歌德曾经说，宁要不义的和平，也不要暴力的正义。刘晓波他们都是学养深厚、明晰历史的人，中国人已经为暴力革命付出了多少代价，即便是六四，当时也是和平的方式，非暴力的思维，这是六四给后来人最可贵的一笔财富，如果当时采取暴力，死的人会更多，中共镇压会更振振有词，六四本身也将蒙污！可叹反思六四的人，因为中国采取暴力镇压措施，几乎就没人来肯定六四的这种非暴力的、现代革命的形式。<br />
<br />
不要忘记历史。更不要鸵鸟现在。楼主把中共当前的统治仍然看作没有任何裂隙的、纯粹高压、窒息式的极权统治，事实上不是这样。请不要熟视无睹！包括你现在在这里发帖，如果是老大哥式的极权主义社会，你早就被抓起来了，还让你在这儿发表你的暴力革命学说？你以为小小的网络技术障碍一个统治政权克服不了吗？而且假如是极权社会，不待你去声嘶力竭（也根本无用）地号召暴力革命，压制人到最基本底线的时候，忍无可忍的时候，以暴易暴会火山爆发一样发生，不会再敲敲你的门和你打声招呼，喊你同去。<br />
<br />
现在社会的特点或者统治的特点是后极权社会。武力弹压、暴力迫害只是在迫不得已时使用，而非像极权社会那样，经常地、大规模地、不分青红皂白地使用。因此，撞破他的底线的一些人士，例如刘晓波等先生，会受到专政。但一般老百姓（除非是发生恶性拆迁等事件）那里暴力恐怖会淡化很多，有比较大的私人空间，你可能会感觉到就业、上学、借贷、住房、医疗等等压力，但这是钝刀割肉，会把人的注意力集中在经济领域，而且通过欺骗性宣传，很多人自认“倒霉不能怨社会”，自身的困境通过经济努力、社会机会，会得以解决。<br />
<br />
人们对现行政权的行动性反抗情绪，远比一些人估计的要低。尤其是在城市，以耗光资源、破坏环境为代价的卑鄙的经济发展，客观上为很多人提供了经济机会，而且房贷、医疗等重重压力，也绑架了广大工薪阶层，使他们根本没有意愿和胆量去进行所谓的暗杀、暴力活动。我所在的社区08年曾经发生过一次惨重的淹车事件，一场夏季大雨让停靠在地下车库、停车场的180多辆轿车受淹，近百台车在80公分以上的水中从凌晨一直淹到次日下午被拖走，15个小时以上，损失惨重。发生事故的原因是：物业公司没有及时报警（它收取停车费、停车场监控费和物业费）；开发商修的这个社区下水管网根本没有与市政管网系统对接，排水不畅；市政对这片地方的道路包括管网10多年不接收不改造，等等。即便是这样，社区的人还是能忍就忍了，最后去打官司的只有7个车主，包括我自己，虽然我们做了大量工作。在我们建立的车主QQ群里，100多号人，大多数既骂共产党，又不认同普适价值和西方民主，认为不符合中国国情，他们骂共产党只是骂贪官污吏，骂社会现象，但大多数人认为中国事情太复杂，有这么个共产党来维持着，也可以。<br />
<br />
你看，即便是被政府、开发商损害到这种样子，人们还是这样，只不过由我为主组织数百人抗议开发商一次，堵路10分钟（那是在奥运前夕，谁堵路抓谁），又撤退。最后偃旗息鼓，只有7个人去打官司。我在QQ群上曾和一些人辩论，就是现在的事是制度问题，应该接受民主自由的制度和思想。但势单力孤，我痛斥爱国主义，被一些人认为不可思议，连国家都爱了？一些人愤而退群。这就是我们面对的实际。从这件事，一方面令人看到国民的麻木，一方面，我深感一些思想，例如爱国主义、民族主义乃至犬儒主义（并行不悖），是中国走向民主自由的最大思想阻障，要想走向现代，必须思想去魅。（我的反爱国主义和QQ辩论文字以前都在猫眼上贴过。）<br />
<br />
不要认为这些居民只是极个别的白领，他们代表的非常广泛。即便是内心反对共产党，很多人都是明哲保身，非常现实，根本不会为了什么理想去冲锋陷阵，更别说某些人一厢情愿怂恿的暗杀、暴力，更别说他们根本不接受、不接触、不了解什么人文理想。道德体系崩溃后，信念体系甚至信任体系也在中国崩溃，而这正是后极权统治很高兴看到、并暗地里欣喜若狂的。<br />
<br />
对于不公平，我接触到的国民通常大骂政府，但不认为这是共产党造成的，有些东西可以通过经济方式进行弥补，比如不是车淹了吗？地没了吗？那你好好做生意挣钱，混得开不就行了。对于不公，大家并不认为它违背了人类的基本理性和情感，而是认为它仅仅是触犯了自己的利益，那么，自己如果会投机钻营，混迹上流，就能改变这种状况。国人在内心深处没有人不是贪官污吏的后备军。对于这样的人，你指望他去搞暗杀吗？<br />
<br />
私人空间的扩大也给了国民以虚幻的自由感。现在公共空间扩大的非常有限，但是私人在小圈子里，在酒桌上，在Qq上，甚至在博客上，言说的空间在增大，甚至从前在极权时代可能掉脑袋的言论，现在是言不彰，官不纠，就是没有形成公共影响力，共产党就一般不会过问，这一方面是因为它的统治效率已经大不如前，另一方面，也源自一种自信和策略，就是给你发泄的空间，这比一味暴力弹压要实惠，暴力弹压只能维护一时，而给你虚幻的自由感觉，让你的能量发泄出去，你的反抗力就会丧失殆尽，而且，他的自信在于，经济主义和实用主义，也就是理想系统的崩溃的让国民根本不愿去倾听任何言论尤其是高调的理想主义、人文主义、自由主义言论，即便他把公共广场的麦克风给你，你已经丧失了听众。在全民中流行的犬儒主义是这样肆虐和强势，体制内的官员不相信任何主义，反体制的精英或者右派，也往往因为内心的虚无奉行一种调侃、玩世不恭，架空任何价值维系，事实上对后极权小骂大帮忙，充当的是体制的同盟军！<br />
<br />
暴力革命没有理性的合法性，也没有现实的必然性。在21世纪它是一种最为非理性的学说，在社会进程中它是一种最危险的演进模式，同时在现实生活中它又是一种最为普通民众所远离的傻瓜行径。当然，我不排除在未来社会进程中可能出现的暴力因素，但暴力仅仅应是和平行动的辅佐而绝非主流，是一种应该排斥的革命异化，而不是值得赞颂的苦口良药，是不确定因素，而不是设计者刻意追求的必然手段。取法乎上，仅得乎中，如果取法暴力革命这个下下之策，不仅不现实，而且它会结出更加怪诞荒谬的果实来。<br />
<br />
我没有药方。我觉得在这样一个时代，有责任、有担当的知识分子应该首先克服绝望，即便民众再麻木，我们都不能放弃用我们擅长的文字、利用后极权给我们提供的言论空间（现在已经不小了，并且还会越来越大），去宣扬普适价值，公民常识，人道主义，用我们自身的行为方式身体力行一种坦荡和仁爱，从而让国民认识到现实生活中还可以有这样的生活方式，这样的人生价值。我也曾经非常绝望，但自从有了儿子，有了我可爱的宝贝，从我们的交流和他的诸多性灵中，我感到人性中有着非常善的方面，也有着对自由的天然向往和追求。推而广之，每当看到父母对儿女那种关爱的眼神，每当感受到生活里那些可贵的友谊，那些难忘的身影，我就会感到普适价值和制度根基于最深刻的人性之中，哪怕在中国，也有这样的人性基础，中国人即便受尽5000年专制统治、受尽狼奶教育、国民劣性根深蒂固，但与全世界各地的人们一样，同属于有着相同特性和品质的人类。<br />
<br />
本来是和楼主留言，想不到打这么多，单独贴一下吧。</div>

							
							
							
							
