													<h2>从“无代表不纳税”这句话与秦晖商榷</h2>
						
						
																											<div id="postmessage_423199" class="t_msgfont">秦晖在《作为“民主条件”的中产阶级：一个彻头彻尾的假问题》中说，“民主的社会基础在于所有人，其动力应是非主非奴的自由人和国家的纳税者。只要政治改革启动，这种人中国从来就不缺。借口我国中产阶级存在与否来拒绝宪政民主，只是某些人的障眼法。”<br />
<br />
秦晖的上述说法正确与否，可以通过“无代表不纳税”这句话出自谁口来证明。<br />
<br />
“无代表不纳税”是英国格言，几乎与《大宪章》一样古老，她伴随了英国的立宪过程，甚至伴随了英国议会下院的诞生过程。现在探究这句话具体是谁说的似乎已经不可能，但判断她出自哪个阶级还是比较容易且意义重大的。<br />
<br />
所以喊出“无代表不纳税”这句话，显然是因为王权对臣民的不断侵犯，导致臣民产生参政议政的念头，因为他们意识到，只有进入议事机构，在法律制定上有了发言权，自己的利益才能少受到或不受到侵犯。那么，怎样进入议事机构呢？他们想到了“税”这个王权赖以维持的东西作为着力点，并用“无代表不纳税”相要挟。<br />
<br />
那么，“无代表不纳税”是哪个阶级喊出的呢？<br />
<br />
首先，正如秦晖所说，农奴和贵族不用纳税。既然如此，这两个阶级喊出“无代表不纳税”的可能性就没有。其次，秦晖说，“民主的动力应是非主非奴的自由人和国家的纳税者”。而英国历史上的自由人是指法人团体、法定城市或自治市中一个有选举权的成员。秦晖把自由人与“国家的纳税者”相提并论有点奇怪，难道自由人不用纳税？如果自由人不用纳税，那么，他们喊出“无代表不纳税”的可能性不也就排除了吗？<br />
<br />
现在剩下的就是“国家的纳税者”了。按照秦晖的说法，这种人就是“第三等级”，或曰“市民，――既包括最穷的人，也包括最富的人，既包括无产者，又包括资本家”。就人性而言，谁纳的税多谁就更关心议事权。最穷的人和无产者纳税少得多，对议事权的关心自然不比富人那么强烈。那么，最有可能喊出“无代表不纳税”的就是最富的人和资本家了。秦晖也承认这是欧洲的立宪规律，但认为不能套到中国，因为中国历来是中央集权，皇权之下人人皆奴，没有一个中等阶级。但，说到这里，秦晖为了避免陷入中国不可能产生民主的危险境地，所以赶紧回到马克思那里，说“我倒更相信这个”（指马克思说资产者与无产者都是民主革命的动力）。无产者革命的动力当然是有的，但这种革命何时产生过民主？没有！那强调无产者的革命动力还有何意义呢？说无产者同样“可以成为渐进的民主化的动力”又有何意义呢？</div>

							
							
							
							
