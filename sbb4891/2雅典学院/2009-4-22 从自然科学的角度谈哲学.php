													<h2>从自然科学的角度谈哲学</h2>
						
						
																											<div id="postmessage_79458" class="t_msgfont">从自然科学的角度谈哲学――仰望星空的启示<br />
<br />
(一)<br />
<br />
人类第一次发出我是谁，我从哪里来，哪里是我的归宿，标志着人类自我意识的觉醒；人类第一次睁着好奇的眼睛仰望星空，则标志着人类对自然界探索的开始。尽管当代科学技术日新月异，从千兆倍速计算机到核子技术、通讯技术，甚至遨游太空，登月飞行。可是，每当人类揭开自然界的一角，就会发现一个广阔的未知领域；人类对宇宙的探求越深入，就会越意识到人类依然处于无知的状态。一般认为，人类仅仅是自然界的一个瞬间的奇迹；尽管有人认为人类是宇宙的脑，是宇宙自身为了被观察而创生了人类。可是现代物理学却向我们展示了一幅恐怖的图景：人类诞生前宇宙已经有了近百亿年的漫长历史，人类现象消失后，宇宙依然要经历百亿年寂寞的时光。<br />
<br />
幸好科学还向我们展现了另一幅图画。我们都知道质能转换公式，也就是说能量和质量都是物质的存在形式。由于质量可见，我们经常习惯于想象宇宙更本质的存在形式是质量形式。从量子力学我们知道，构成微观世界的基本粒子并不是我们能想象出的实心粒子，而是一个个能量包。如果我们是由能量包构成，那么，宇宙的常态其实是能量，而所谓质量转化作巨大的能量却是巨量能量包能级跃迁的结果。因此，我们世界的图景完全不是直观告诉我们的那个样子，我们看到的实物其实是一个个的能量聚积块，甚至我们每一个人都是一团能量。<br />
<br />
我们是空心的，足够纤细就可以不损伤我们而从我们的身体中穿过！<br />
<br />
问题还没结束！<br />
<br />
世界由什么组成？一般的回答一定是物质，并且大脑中浮现出来的是质量实体的图像。通过上面的讨论，我们头脑中的图像可能会发生一些变化：图像中除了实体质量，还会有能量，甚至能量团。可是现代科学还告诉我们，信息也是物质的一种存在形式。为什么信息是物质存在的一种形式呢？因为不具备形式的宇宙是热寂状态；而热寂状态是各向同性，无差别自然也没活力。然而我们看到的世界是一个丰富多彩，充满差异；差异之别叫做形式，差别的特征叫做信息。非常有意思，原来我们几乎从泰勒斯经过赫拉克利特和德谟克利特径直走进莱布尼兹的世界，现在差不多又回到亚里士多德的形式是事物的本质的形而上学世界。于是我们的世界就成了这样一幅图像：质量、能量、信息共存并在不断相互转换中。<br />
<br />
<br />
 （二）<br />
<br />
现在我们就进入到一个新层面。我们的世界不但由实体组成，而且包括了能感觉其存在，却无影无形的能量和信息。根据同样的逻辑，现在我们要作出另一个重要判断：这个世界还有另外一种存在，这个存在同样地无影无形，却无时无刻地在我们身边，并且支配着万物，这就是精神世界。从柏拉图的理念开始，到奥古斯丁的上帝之城，再到黑格尔的主观理念、客观理念和绝对理念，其实论及的就是这个存在。<br />
<br />
我们经常会有这样的疑问：我看到的红色，为什么就知道别人看到的红色就是我感觉到的那种颜色？一件事物的感觉能够与其他人分享的基础是，我们先天地认为他人和我的感觉的一致性。比如我被针刺痛，我知道别人被针刺会有同样的感觉。但是，这种认识并不自明，除非我们能找到一个共同的感知基础，这个感知的基础只能是：我们共享同一个感知体，我们都是这个巨大感知体的一部分。这有些像斯宾诺萨的泛神论，但是肯定不是斯宾诺萨。这其实更接近柏拉图的理念说，也更接近黑格尔的绝对理念。<br />
<br />
如果我们的推理是正确的，那么我们必然得出一个结论：世界除了由质量、能量、信息构筑之外，还存在一个更本质的、弥漫于整个宇宙，并且体现出一致性的存在，这就是宇宙精神；而人类的精神只是对宇宙精神的一种分享。<br />
<br />
由此我们摆脱了现代物理学的恐怖推断。世界并不寂寞，从世界创生之日起，就有一个更本质的形式在支配万物，并表现出严密逻辑的理性。否则，我们不可能断言A处1+1=2，在B处该等式依然成立，我们也不可能通过逻辑判断来推断事物的真伪。正是有了这个精神世界的一致性，才可能超越空间的局限，也超越个体的分割，把浩瀚的宇宙统一起来，我们能够通过回忆和想象回到过去和展望未来；也正是由于这种一致性和统一性，我们才那么确信他人的感知与我么们自己感知的同质。<br />
<br />
从上面的讨论我们是不是能得出这样一个结论：人类现象只是世界精神的一个表现形式，人体只是精神的一个载体，人类现象出现和消失只是精神载体的转换？<br />
<br />
如是，那么科学必然最终与宗教殊途同归，并从不同的角度探索宇宙这个巨大的秘密。<br />
<br />
（三）<br />
 <br />
这样一来，寻求宇宙真相的工作，既是人类的智力活动，也是宇宙自身作为其存在属性的精神活动的一部分，因此人生有了价值。人类智性即宇宙存在的智性，这种智性对宇宙秘密的探求不会因为人类的消亡而停止，更不会因为突然的宇宙灾难而中断。因此人类的存在才有了意义，尤其是人类的精神活动，包括知性活动和感性活动具有宇宙本质上的意义。关于感性活动的意义，后面会专门提及。<br />
<br />
人类各个民族从远古就开始了对宇宙秘密的探索。但是大多数民族的探索局限于感觉和联想阶段，这就是神话阶段，并且最终演进为宗教。中国古代虽然出现了哲学思考，如老庄和墨子等。但由于儒家思想的强大影响，这种哲学思考很快脱离了对经验的解释和经验细节的考察，而是转为用天象解释社会现象。其结果是，哲学停滞于道德和政治伦理层面，最终阻碍了哲学思想的发展，也阻断了科学思想的形成。<br />
<br />
西方初民也没从一开始就摆脱感觉和联想阶段，这个阶段的产物之一是犹太教，并分支出基督教；犹太-基督教成为西方文化的基础之一，涵盖了从幼发拉底河和低格里斯河产生人类文明后，人类文明主流两万年间积累的道德伦理思想和对世界的看法。这一智性产物对以感性为基础的人文科学，如文学、绘画、伦理道德、古代政治形态等产生了持续的影响；人类感觉-联想阶段的另一产物则是希腊神话；奥林帕斯山上的众神，完全拷贝了人间的生活和等级关系。甚至人类的弱点也在众神身上得到体现。<br />
<br />
由于爱奥尼亚地区频繁的航海贸易，产生了对天文学研究的需求，这种需求无法仅仅通过感觉-联想和讲故事来满足。人们需求一套精确的方法，以避免迷航和遭遇风暴。这一探索让人类智性从另一方面异军突起，这就是人类理性出现了飞跃,包括以泰勒斯-巴门尼德-柏拉图为代表的形而上学思考，以普罗泰格拉-欧几里德-毕达格拉斯为代表的逻辑学（数学）和后期以亚里士多德和阿基米德推动的呼之欲出的经验科学。<br />
<br />
（四）<br />
<br />
凡人类思维建构有形式概念-风格概念和事实概念-法则概念，只有前者的发展，才能带来后者的出现和发展，而只有后者发展之后才能促进前者的飞跃。这是人类认识自然的逻辑。<br />
<br />
人类自非洲进化完成后，为了扩展生存空间，其中最有冒险精神的一支北上，直至尼罗河流域，在此期间由于尼罗河的灌溉条件，从而促发了农业的发展。人类有了生产剩余后，由于分配的需要，为了扩大交流，计算收成以及分配等，发展出了象形文字这种初级文字和算术；生产剩余促进了人口的增长，因而造成了生存危机。一部分人类不得不继续跋涉，其中一部分在幼发拉底河与低格里斯河两河流域定居下来，一部分人类则扩散至东亚和西、北欧一带，驱逐与同化了当地的北京猿人和尼安德特人，形成了新人种。<br />
<br />
两河流域的人类由于生产的需要，开始了畜牧业，产生了灌溉农业。由尼罗河流域和两河流域围绕而成的地中海地区，获得了得天独厚的条件：受尼罗河流域和两河流域的文明哺育，同时受到地中海航海贸易的刺激，地中海地区的人类思维能力获得了巨大发展，成为人类智力成长的摇篮。近代以前，几乎所有重要的人类发明都源于尼罗河、幼发拉底河、底格里斯河流域等环地中海地区，之后才向西欧和东亚扩散。因此，欧洲文明必然成为人类文明的主流文化，而东亚文明、印第安的马雅文明、印加文明等（这些印第安文明的意义以后还会提到）也就只能占据各种亚种文化的地位。<br />
<br />
拼音文字的出现象征着该地区人类发展出至关重要的抽象能力。现代语言学和现代哲学研究表明，人类语言与客观世界同构，语言是客观世界在人类精神世界的投影。因此，语言的形式不但反映了一个民族的思维模式，也反过来影响应用这种语言的人们的思维方式。西方拼音文字的出现，反过来影响了西方人的思维方式，对人类文明的发展产生了深远的影响，在公元前500年左右出现了人类认识能力的质的飞跃。<br />
<br />
<br />
（五）<br />
<br />
东亚民族由于较早脱离了人类主体，并且由于喜马拉雅山脉和西部沙漠戈壁地区的阻隔，只有农业、畜牧等实用技术得到传承，精神活动中始终缺乏抽象能力。因此，东亚民族的文字一直沿袭尼罗河流域时期发展出来的象形文字，以象形文字为代表的语言系统则极大地制约了东亚民族的思维能力。<br />
<br />
象形文字构成的语言系统不可能发展出抽象的思维，对事物的认识也只能停留于类比和联想阶段。比如，中国对自然界认识具代表性的思想是五行生克，金木水火土构成了世界的元素，这是一种表象性的直观感受，没有对自然界的抽象性思考；五个元素的相生相克则完全是联想式和比喻性的。<br />
<br />
由于印度不存在地理性的天然障碍，在伊斯兰教造成东西方文化隔离之前一直与西方世界保持畅通的联系，亚历山大时期一度成为希腊世界的一部分。因此，印度思想中不乏抽象思维。其发展出的国际象棋和传至中国经改造后的中国象棋可以看出两个民族思维的差异――国际象棋的法则概念和中国象棋的形式概念。这可以解释印度能够从神话这种人类的想象世界走出，通过抽象发展出成为世界三大宗教之一的佛教；而中国人依然生活于玉皇/王母-老君-十二星宿的神话世界。通过比较我们可以看出，中国的道教众神与荷马时期奥林帕斯山上的众神具有极大的相似性。也就是说，中国自己的宗教思考一直停留在人类童年的思维能力。<br />
<br />
（六）<br />
<br />
在东方对世界的本质停留在类比和联想阶段的同一时期，古希腊的哲学思辨则显露出抽象思维的曙光。<br />
<br />
泰勒斯说：世界的本质是水。虽然看起来泰勒斯的世界的本质是水与中国的世界由金木水火土构成在思路上极为相似，然而二者之间却有着本质差异；中国的五行说来源于人们对事物表象的认识：世界上有金属性物质，有木本植物，有湖泊河流和与雨水露水，有电闪和火焰，有土地。因此，五行说是对表象世界的归纳性解释。但是，这种认识并不能穿透物质世界的表象，因而也不可能归纳出物质世界本质性的异同。<br />
<br />
而泰勒斯的水为世界的本质却具有对事物理解的穿透力。泰勒斯不但看到了万物的生存离不开水，也看到了水在生命世界和无生命世界所占的巨大分量，更看到了水的无定形，因而可以构成万物。即使从今天的科学观来看，泰勒斯把水看成世界的始原也有其深刻的道理。我们知道，海洋孕育了地球的生命，没有水则一切生命活动都将停止；我们也能看到，地球的表面70%被水覆盖，我们人体的70%由水组成。其实每一种生命的70%都是由水组成。水是一切有机活动，包括人类思想活动的载体。我们寻找地外生命时寻找的重要物质，就是看这个星球是否有水存在。<br />
<br />
阿纳克西曼德和阿纳克西美尼从物相变化和变化的动因角度提出了火和气等同样是是世界本基的想法。西方思想史上最大的特点是，许多对立性的观点，其实是一种补充观点，或曰诠释性观点，随着分类学和逻辑学的发展，冲突性和诠释性才得以明确区分。阿纳克西曼德和阿纳克西美尼对泰勒斯的观点就属于一种诠释性的，但其中蕴含了一种对物质世界更深刻的理解，这种理解一直被后人所忽略。这两个人的深刻性在于，其思想中隐约闪现了能量参与物质的物相转化，并且是其中更为基本的原因。<br />
<br />
