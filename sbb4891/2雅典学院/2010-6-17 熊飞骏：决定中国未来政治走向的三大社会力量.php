																											<div id="postmessage_686954" class="t_msgfont">决定中国未来政治走向的三大社会力量<br />
&nbsp; &nbsp;――熊飞骏<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;今天的中国社会，有三大社会力量将决定未来中国的政治走向。他们是：<br />
&nbsp; &nbsp;特权集团，自由派知识分子和毛左愤青。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;一、中国社会的主宰者――特权集团<br />
&nbsp; &nbsp;特权集团数量很少，但他们掌控着各地方各部门的政治经济大权和绝大部分国家资源。他们以1%的极少数掌控着80%以上的社会财富（一说0.4占有70%的社会财富）。其中的800名顶级富豪就占有20%的社会财富。<br />
&nbsp; &nbsp;相比之下，占总人口99.9%的平民百姓只拥有20%的社会财富。<br />
&nbsp; &nbsp;让人不能接受的是：特权集团拥有的巨额财富只有极少一部分是通过勤劳智慧所得；绝大部分财富则是通过“公权私用”的非法手段聚敛的。<br />
&nbsp; &nbsp;特权集团掌握的巨额财富与他们对国家社会的贡献不成比例。中国的“个人所得税”比例最能说明问题，占有80%以上巨额财富的特权阶层邀纳的个人所得税不到5%；美国富豪的比例是1%，占有的社会财富比例不到30%，可他们承担了30%以上的个人所得税。中国的工薪阶层缴纳的个税比例高达50%；美国的同一数据则不到5%。<br />
&nbsp; &nbsp;改革开放的后十年，中国的贪污腐败成燎原之势，经济高速增长的成果绝大部分为特权阶层通过非法手段攫取。广大平民尤其是城市市民则在住房、教育、医疗等“新三座大山”的压迫下日益陷入实质上的贫困。特权阶层和平民百姓的矛盾日益加深，并以令人担忧的速度走向尖锐化的对立。<br />
&nbsp; &nbsp;特权集团除了掌控着无可匹敌的公权和财富外，还掌控着无可匹敌的“话语权”。几乎所有的影视电台和报刊杂志等大众传媒工具都为他们服务的。<br />
&nbsp; &nbsp;特权集团的主体舆论导向是“稳定”和“发展”；同时为了转移国内视线不时对某法治国家提出“严正抗议”。不过这些抗议多是口不应心的，都是说给国内没有独立思维能力的群体听的。中国是一个不爱思考的民族，没有独立思维能力的群体占国民的绝大多数，所以这些“严正抗议”有时能引发全国性的排外浪潮。当被煽动起来的“人民群众”闹得有点过火，威胁到特权集团的实际利益时，“点火”者又不得不站出来“灭火”，因为他们的自身利益与“严正抗议”的某法治国家有千丝万缕的联系。<br />
&nbsp; &nbsp;因为拥有更多“知情权”的缘故，特权集团对自己的“地位”并没有媒体宣传出来的那种“信心”。他们当中的很多人拥有较强的“个人危机意识”，既担心“动乱”和“突发事件”；又担心贪腐罪行“东窗事发”被集团同类当作“替罪羊”。在这种“危机心态”主使下，很多人利用职权把财产和子女家属转移到他们曾“严正声讨”的那些法治国家。“坏人心中也有杆称”，尽管他们口头上“严正声讨抗议”某国，但内心知道这些法治国家比自己的国家理性健康得多。尽管他们在媒体上大力声援北朝鲜、缅甸、古巴等专制独裁国的“独立自主”和“内政不容干涉”，但内心知道它们都是些忘恩负义过河拆桥的“无赖混混”、“白眼狼”和“王八蛋”，杀死也不会把财产亲属转移到这样的国家。不但不会移亲到缅甸、北朝鲜，甚至连谋求结为“国际统一战线”但民主法治成果还不稳固的俄罗斯也不会去。特权集团首选的移亲国家第一是美国，第二是加拿大，第三是澳大利亚，都是成熟的民主法治国家。<br />
&nbsp; &nbsp;上述例子说明一个人的内心价值取向是由他的“行动”决定的，“对外言论”多数言不由衷。所以一个成熟的公民要少听上面“怎么说”多看上面“怎么做”。<br />
&nbsp; &nbsp;所以别看今天中国的媒体舆论走向是“亲俄反美”，但中国绝不会回到毛中国时的外交政策对俄“一边倒”，因为中国外交政策是由特权集团决定的，而特权集团的财产和亲属多数在美国不在俄罗斯。单从个人利益来说，特权集团也不会冒着资产被冻结子女受歧视的风险和美国决裂。如果站在国家民族利益的高度，“走向美国”更有利于中华民族的根本利益；亲俄则是“与狼共舞”。美国没有侵占中国一寸领土而俄罗斯则侵占了中国160多万平方公里领土并直接策动了157万平方公里的外蒙古脱离中国。<br />
&nbsp; &nbsp;特权集团虽然利用手中的职权干了很多伤害国家民族的事情，但他们并非象毛左愤青断言的那样都是天性邪恶之辈。任何人都是天使和魔鬼的复合体，修其善则为善，修其恶则为恶。是“奖恶惩善、劣胜优汰”的专制体制限制了他们发挥灵魂深层的“天使”力量。我在《贪官也是旧体制的受害者》一文中较为详尽地阐述了“人在官场、身不由己”的贪官悲剧。<br />
&nbsp; &nbsp;特权集团不但不是天性邪恶之辈，相反在集团内部还存在部分有远见卓识和较强民族责任心的开明人士。他们是未来中国的希望。中国的社会转型离不开这部分人士的配合与努力，或者离不开他们的“领导”。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;二、中国社会的启蒙者――自由派知识分子<br />
&nbsp; &nbsp;决定未来中国政治走向的第二大力量是自由派知识分子。他们是政府和特权集团的主要批评者，是民主法治精神的主要传播者，是开发民智的启蒙思想者，是中华民族文明进步的灯塔。这部分人的数量和特权集团一样少。如果说特权集团拥有过人的权力财富；那么这部分人则拥有过人的才能见识，拥有不受官媒影响的独立思维能力，对中国存在的问题和解决问题的办法有较为清醒的认识。他们中的多数属中产阶级，拥有较为体面的工作和一份稳定的收入，生计和尊严不成问题。他们之所以担当很大的风险充当批评政府的角色，主要是灵魂深层的爱国情怀和民族责任心使然，对推进中华民族文明进步的热情远远大于个人富贵享受的热情。所以自由派知识分子是最真挚的爱国者；也是最有责任心的民族主义者；尽管他们经常被毛左愤青污为汉奸卖国贼。<br />
&nbsp; &nbsp;自由派人士的观点文章很难被电台报刊等大众传媒公开发表。他们只能借网络这块平台来表达自己的忧国忧民情怀。近五年时间，自由派知识分子在网络的言论自由度受到越来越严格的限制。他们在论坛发文越来越困难，连天涯、凯迪等一度相对开明自由的论坛也对他们的文章设下了“五关六将”。一篇内容很普通的文章都可能因含有“敏感词汇”无法响应“发送”请求……<br />
&nbsp; &nbsp;自由派人士对“权力”没有多少欲望。他们批评政府和专制文化不过是充当“体制森林”的啄木鸟角色，实质上是执政府“勇于说真话”的参谋和智囊，丝毫也没有“取而代之”的野心。<br />
&nbsp; &nbsp;自由派人士反对流血的暴力革命，主张在中国推行民主法治式的政治变革，但他们的改革设计是循序渐进式的，不主张在中国一下子实行“全民直选”；而是从“基层政务官直选”、说真话政府、新闻自由、司法独立逐一实践。他们深知在没有作好必要准备的情况下做过于猛烈的急刹车，没有经受任何民主历练的普通国民缺少必要的政治眼光，“报复嫉恨”的情绪主宰政治情感，很可能上演巴勒斯坦选出“哈马斯”的悲剧。本人在近年有幸被划为自由派写手，拙作《中国的民主之路》就表明了渐进式政治变革的主张。<br />
&nbsp; &nbsp;自由派人士对特权集团并没有毛左愤青那种“深仇大恨”心理。他们撰文抨击特权不过是基于“中华民族文明进步”的良苦用心，没有取而代之的野心驱动。他们的主张虽然不利于特权集团的“急功近利”，但有利于特权集团的长远利益。他们不但不主张对特权集团实行毛左式的“流血清算”，相反还希望特权集团中的精英人士能给中国的民主变革提供领导。<br />
&nbsp; &nbsp;自由派知识分子的最大“软肋”是缺少群众基础。他们是一群“理想主义”者，以唤醒国民、启蒙民智为己任，对浸染“实用主义”、“功利文化”和“官本位病毒”的普通国民没有多少号召力。他们的文章以“说理”为主，而讲明一个道理往往需要很长的的篇幅。今天的中国人有“不爱阅读”的毛病，没耐心看完他们不具煽惑性的长篇大论文字。所以自由派知识分子的文章读者并不多。如果今天的中国突然实行全民普选，自由派人士远远不是毛左愤青的对手。<br />
&nbsp; &nbsp;自由派知识分子数量少且没有多少权欲，对“政权”构不成实质性的威胁，是最远离“功利”的一大社会力量。<br />
&nbsp; &nbsp;自由派知识分子以黎明、杨恒均、韩寒为代表，体现了老中青式的“接力棒组合”。从唯心的角度来看，自由派知识分子后继有人，是一个有希望有生命力的团体。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;三、中国社会的思想毒瘤――毛左愤青<br />
&nbsp; &nbsp;改革开放的最后十年，中国步入了堕落的时代。燎原烈火样蔓延的权力腐败消耗了经济高速增长的成果，繁荣发展的背后潜藏的社会风险呈几何级数增长。<br />
&nbsp; &nbsp;贪污腐败的加速蔓延催生了一个毛左集团――未来中国的最大风险种子。这个集团的主体是奴化教育下成长起来的失意青年和城市无产者，一个有很强失落感的群体。<br />
&nbsp; &nbsp;毛左集团的社会情感特征是强烈憎恶贪污腐败，对特权和赤裸裸的社会不公正义愤填膺；同时仇视整个上流社会。毛左集团基于知识和阅历的缺陷，不可能了解社会不公正的真实根源是落后过时的专制体制；消除社会不公正的不可替代良方是民主和法治。因此毛左们一方面大骂特权和腐败；一方面又不自主地跟着特权集团的舆论导向把攻击矛头对准与他们的切身利益没有任何利害关系的自由派知识分子和西方法治国家等“假想敌”身上，不自觉地成为特权集团在舆论界的“冲锋队员”。<br />
&nbsp; &nbsp;毛左分子既渴望乌托邦式的社会平等；又渴望自己能跻身为特权阶层的一员。基于这种“吃不到葡萄说葡萄酸”的矛盾心态，他们对特权集团既恨又怕。一方面极为仇恨贪官权贵，恨不能把特权阶层砍尽杀绝；另一方面又深怕得罪能主宰他们命运的体制内人物，力求在舆论导向上和官媒保持一致，希望自己的吹鼓手角色能引起上层的注意。<br />
&nbsp; &nbsp;毛左分子虽然游离于体制大门之外，但身上却感染了专制体制的所有病菌，在制造谎言、耍阴谋搞小动作和不择手段方面一点也不亚于体制内的腐败分子。因此他们身上集专制体制劣根性之大成，并且因其行为极端不顾后果比腐败分子更具破坏性和危险性。<br />
&nbsp; &nbsp;毛左分子热衷于制造谎言和造谣惑众，尤其是在美化他们的精神领袖毛太阳和丑化西方法治国家时谎话连篇，无视最基本的历史国际常识。在造遥惑众方而则远远不是“无中生有”一字能够概括的。近来毛左骨干在网络扯起了迷天大谎，说什么右派阴谋对左派实施大屠杀？纵观左右两派的网络言论，多数情况下都是毛左分子鼓吹暴力喊打喊杀的，自由派知识分子则旗帜鲜明地反对血腥暴力。从某些毛左分子的不择手段来看，我总算领略到了什么叫“贼喊捉贼”。<br />
&nbsp; &nbsp;毛左分子的行为特征是盲目排外；打着“爱国”和“民族主义”旗帜，干着伤害国家民族的“红卫兵”式暴行。他们的骨子深层欺软怕硬，在“爱国”表演上总是选择最安全的方向，对没有反抗力的假想对手慷慨激昂。<br />
<br />
 绝大部分高呼爱国的毛左愤青，实际上只是跟在爱国旗帜后面瞎起哄。他们组成偌大的群体，攀附主流媒体的舆论导向，对与主流媒体不一致的声音乱扣帽子乱打棍子，让自身的卑劣本性有机会释放。至于对祖国的种种不足或恶劣之处，他们是不敢言或不知如何言说，因为祖国有第一流强大的国家机器等着他。所以愤青式爱国是欺软怕硬的，是披着硬盔甲的软骨头。历史的经验一千次证明，凡是欺软怕硬者都不可能成为坚定的爱国人士！<br />
&nbsp; &nbsp;一位网民这样评价毛左愤青的“爱国”。<br />
&nbsp; &nbsp;“我们绝对知道在什么时候可以拍案而起做出义正词严状，也绝对知道什么时候必须对自己清楚万分的问题保持沉默、三缄其口。我们还有一个更可怕的表现。这就是柿子专拣软的捏：即在一个最安全的方向上做出好似怒不可遏、仗义执言实际精打细算、八面玲珑的完美演出。我们也知道什么时候说什么话可以上达天庭得到首肯，什么话会触犯众怒。就我自己而言，这种算计已经高度技巧、出神入化；这种掌握已经进入潜意识层面。”<br />
&nbsp; &nbsp;所以毛左愤青的爱国在本质上是表演给他们切齿痛恨梦想取而代之的特权集团看的，希望他们的行为能得到特权人物的青睐，把他们的精彩表演者纳入特权阵营……<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;毛左分子多是一些没有独立思维能力，对中国的真实历史一无所知的洗脑者。他们把已故的毛太阳奉为他们的精神领袖，因为毛领袖在文革期间把特权阶层的绝大多数划为牛鬼蛇神打倒在地，任红卫兵愤青批斗凌辱。他们不可能知道今天的中国之所以出现难以容忍的腐败和不公正，恰恰是毛太阳当初缔造的那套专制体制结成的恶果。他们梦想中国出现一位新的毛式独裁者，领导他们向特权集团反攻倒算并最终取而代之；同时把整个上流社会踩在脚下。毛左愤青要么是不了解真实毛领袖的小青年，他们心目中的毛领袖不过是影视剧中唐国强扮演的那位指点江山激扬文字的大英雄，与真实的毛领袖相去甚远；要么是风光不再的文革既得利益者。<br />
&nbsp; &nbsp;毛左愤青虽然没有掌握电台报刊等大众传媒工具，但在网络上却能自由兴风作浪。他们的文章尽管文理不通，有的甚至就是几句骂人的脏话；但因揭露了社会的部分不公正现象和充斥着似是而非的伪命题，极容易混淆视听，对不爱阅读不肯思考又有潜在暴力倾向的普通国民来说有很大的煽惑性。随着社会不满情绪的日益高涨，毛左愤青的舆论影响力也水涨船高。<br />
&nbsp; &nbsp;毛左分子多数没有撰文才能，因此热衷于在网上灌水骂人。灌水是不需要水平的，只要会扣帽子打棍子会骂脏话就成。毛左分子在文章后面写回复与自由派知识分子不同，自由派知识分子多数亮明身份；毛左分子则匿名躲在背后骂人。从这点可看出毛左分子的阴暗虚弱心理，相比之下自由派知识分子的行为则要光明磊落得多。<br />
&nbsp; &nbsp;和自由派人士不同，毛左分子对权力有很强的欲望。他们骨子深层的帝王思想和专制情结决定了他们是一台追逐权力的冷酷无情机器。他们对特权集团怀有不可化解的“深仇大恨”，但他们仇恨的是“特权人士”，而不是“特权”本身。如果让他们得势，一样会大搞“特权”，对“特权”的爱好一点也不亚于今天的特权集团。同样是抨击“特权”，但目的有天壤之别：自由派人士是为了在中国取消“特权”；毛左愤青则是梦想取而代之。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;四、走向未来之路<br />
&nbsp; &nbsp;今天的中国社会，特权集团掌控着公权和社会财富；毛左愤青操控底层社会的民意；自由派知识分子则把握着中华民族长治久安文明进步的钥匙。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;毛左分子基于无知和势利的双重因素，把攻击的矛头不是对准“社会不公正”的始作甬者特权集团，而是首先对准了同样受到不公正待遇的自由派知识分子。自由派知识分子起初本无意和毛左愤青分庭抗礼，但在毛左分子不分青红皂白的疯狂嘶咬下被迫应战还击。于是最不应该搞对抗的两大集团相互嘶咬得不可开交；特权集团则乐得隔山观虎斗。<br />
&nbsp; &nbsp;毛左分子和自由派知识分子的主要争执体现的在对外策略上。自由派知识分子多数亲美防俄，对北朝鲜、缅甸、古巴等无赖小混混厌恶反感。毛左分子则一律反美亲俄，把金家王朝、缅甸军政府和卡斯特罗当作一条战壕里的阶级兄弟，甚至无视最基本的国家安全常识鼓励金胖子拥有核武器？对于左右两派在外交争执上的是非，特权集团心知肚明。他们深知毛左分子鼓吹的“西方亡我之心不死”是不存在的，英、美等法治国家对中国并没有什么狼子野心，否则他们也不会把美国当成财产亲属的首选避难所。他们之所以在大众传媒上经常对美英两国“严正抗议”，主要原因是对方的民主法治体制成功地“把政府送进了笼子”，成功的遏制了贪污腐败，消灭了特权。他们害怕中国人了解这一科学的政治体制并起而效之，威胁到他们的特权地位。基于这一自私的动机，特权集团常常利用毛左愤青的排外狂热来诋毁英美，把子虚乌有的“美国威胁”植入普通民众的潜意识深层，从而不能理性认识美国法治体制的科学性与合理性。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;特权集团为了长期把持自己的特权利益，巴不得左右两派斗得越火越好。他们在左右两派之间玩平衡术，并且把天平不明智地向左边倾斜。自由派知识分子忧国忧民的文章动则因“敏感词汇”在网络“被拒”或“被删”，甚至封杀博客。毛左分子公开否定改革开放，公开叫嚣“反邓”和为“四人帮”鸣冤叫屈的文章居然能通过审查？这真是咄咄怪事？<br />
&nbsp; &nbsp;对于特权集团来说，自由派知识分子不过是体制森林里的啄木鸟；毛左愤青则是吞噬毁灭一切的蝗虫。如果自由派知识分子得势，特权集团付出的代价只不过是不能继续享受特权，多数的生命尊严则有保障，很多人还能保住部分甚至全部既得利益。前苏联的特权集团在自由俄罗斯的命运就是最好的例证。如果毛左分子一朝得势，绝大多数特权人物则会被赶尽杀绝，亲属也会受到残酷的清洗。就算不是站在国家民族利益的高度，单从特权集团自身的狭隘利益来说，特权集团也应该和自由派知识分子结盟，把毛左愤青扫入历史的垃圾堆。<br />
&nbsp; &nbsp;这里有必要拿法国大革命的两大政治力量来对比：自由派知识分子相当于立宪派。立宪派得势不过是限制王权增长民权取消民众痛恨的特权；但皇帝路易十六照样是行政首脑，特权集团的人身尊严和既得利益受到了尊重。毛左愤青则相当于雅各宾党人。雅各宾党一朝得势，皇帝路易十六即刻被押上断头台；所有的特权集团都在断头机下身首异处。亲人被无情屠杀，财产被没收充公。<br />
&nbsp; &nbsp;道理虽然明摆着，但在“奖恶惩善、劣胜优汰”专制体制的长期作用下，跻身于特权阶层的权力人物并不都拥有那样的智商和眼光。如果某位势利短视的顽固分子得势，极有可能雇佣蝗虫来对付体制森林里的啄木鸟。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;中国社会转型的理想之路是：特权集团中的精英人物和自由派知识分子结合，由特权精英中的“命世英雄”提供领导，在中国推行“明治维新”式的民主法治变革。<br />
&nbsp; &nbsp;这条转型之路其实就是百年前的“光绪皇帝”之路，如果成功将是中华民族的千年福祉。中国将自此踏上长治久安文明进步的阶梯，为改朝换代进行的周期性暴力革命将一去不返，中国人将走出周期性“自毁文明”的怪圈，文明成果将长久积累下去并完成量变到质变的飞跃。二十一世纪将真个成为“中国世纪”。<br />
&nbsp; &nbsp;俄罗斯的转型模式已经证明了民主变革是特权集团和平民百姓的“双嬴”之路，特权集团不但逃脱了“清算报复”的“循环报应”噩梦，还保住了大部分既得利益。平民百姓也普遍走出了“饥饿”和“疯狂”，公平享受经济增长的成果，成长为幸福感和尊严感远超中国的“现代公民”。<br />
&nbsp; &nbsp;中国社会转型的最坏之路是：特权集团中的极端腐败顽固分子为了维护自己的“急功近利”，不惜和极具破坏性且不留后路的毛左愤青结盟，向忠言逆耳的自由派知识分子大开杀戒，建立高度集权式的独裁政府。<br />
&nbsp; &nbsp;这条转型之路实质上是慈禧太后的“义和团之路”和毛太阳的“文革之路”，结果是给中华民族带来空前的灾难。<br />
&nbsp; &nbsp;独裁之路的受害者不但是自由派知识分子，对特权集团也是灭顶之灾。这条路如果失败，特权集团就会象满清权贵一样受到残酷无清的报复清算，不但丧失所有的既得利益，多数还会搭上自己的性命。如果象毛一样取得暂时的成功，毛左愤青的屠杀机器一旦开动，就不会自动停下。他们的“偶像领袖”为了赢得毛愤的支持，同时也为了把权力集中到自己一人身上，就会象毛一样把绝大多数特权家族定为“牛鬼蛇神”批倒批臭，然后送上监狱刑场。<br />
&nbsp; &nbsp;毛左是一群只会破坏没有建设才能的群氓，任何统治者都不可能长期容忍他们，等利用价值一完后就会把他们“一锅烩”。慈禧把义和团赶尽杀绝；毛领袖把帮其夺权的所有“红卫兵”送往荒僻落后的农村“接受贫下中农再教育”；希特勒则干脆把自己的打手“冲锋队员”集中起来用机枪扫射……<br />
