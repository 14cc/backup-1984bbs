																											<div id="postmessage_779413" class="t_msgfont">为什么有人盼望成龙去死？<br />
刘松萝<br />
<br />
据报道，成龙在谈到代言“霸王”洗发水问题时说，“有些媒体就特别针对我们这几个艺人，很奇怪，好像最好我们全部死掉。”<br />
<br />
长期以来，成龙自我感觉甚好，缺少自知之明。商业上的操作，媒体的胡乱吹捧，让成龙不知道还有很多不喜欢他的人。知道不得人心以后，还是过于自恋。其实，他没有那么大的力量，干不成天大的坏事，并没有被人恨的资格，充其量令人生厌罢了。成龙尽管放心，没有很多人盼着你去死。<br />
<br />
不过，商业的力量虽然强大无比，能够让一个早就应该过气的人仍然闪闪发光，让一大群人去追捧，却无法阻止人们生出倦意来。成龙看不到人心的变化，还要到博鳌去讲他根本不懂的问题，讲什么“中国人是要管的，否则便会为所欲为”，效果当然是灾难性的。<br />
<br />
成龙算不上坏人，也谈不上虚伪。他在慈善事业上的贡献，是应该肯定的。他的问题是头脑简单，以为凡是让他合算的事情不但一定会对中国有利，而且还会对所有的中国人有利。在这里，也不排除天真或者自我欺骗的可能性。与此同时，成龙还是精明的。成龙得到的东西，可以说数倍于他的付出。可以进入兵马俑坑拍电影，可以“买一赠一”，带着他那个没有什么灵气的儿子一起上央视春节晚会，应该说是过分了。<br />
<br />
至于代言“霸王”洗发水的问题，成龙有些做作。在广告中，成龙说：“霸王请我做这个广告，其实一开始我是拒绝的。”这种风格，就是典型的成龙式做作。成龙说，“现在国家的合格批文都已经下来了，那些谎言就不攻自破了。”对此，网友“风餐露宿”指出，“霸王”洗发水中二恶烷没有超标。“不过问题就来了，霸王不是号称纯中药洗发水嘛。”<br />
<br />
最后要说的是，假如真的有个别人盼着成龙去死，那么除了那些人内心邪恶之外，我猜想还是因为只有死亡才能阻止商业的魔力，让我们远离不喜欢的人。<br />
<br />
成龙谈到了“我们”，也就是几个艺人。对此，我想谈一谈亲身的感受。记得那位小品之王患病时，我一面希望他康复，另一面又对那么多媒体的无微不至感到不解。毕竟，在我们的社会中需要雪中送炭的事情太多了。当我看到小品王再一次莅临春晚，吃力地扮演那老一套的节目时，心中生出恶意。这恶意不是诅咒他遇到不测，而是希望他康复得慢一些，为了我们的除夕，也是为了让他充分休息，颐养天年。<br />
<br />
2010.8.12.零时</div>

							
							
							
							
