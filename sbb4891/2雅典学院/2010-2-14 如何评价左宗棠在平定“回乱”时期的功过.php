&nbsp; &nbsp;&nbsp; &nbsp; 第二点中，这点就完全是以暴制暴的野蛮行为，这就是人类在几千年的进化过程中努力解决的问题之一，这也是国家公诉制度产生的重要原因之一。还是上面的例子，弟弟的老婆在弟弟砍死哥哥时没有阻止，父母不但砍死了弟弟又砍死了弟弟的老婆，这父母不就是禽兽吗？ 既然都是禽兽了也就没有正义不正义了；从个人的情感角度来讲，回民掀起的大屠杀确实有足够理由成为满族的左宗棠及其的主体为汉族的湘军报复的理由，但是我们现在讨论历史问题时拿历史人物的情绪作为评判的理由的话是很可笑的，因为历史人物的情绪可能有很多种，左宗棠或许本来就是个杀人狂，以嗜杀为好呢？ 所以这个说法也是站不住的；<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;第三点中，辛德勒的行为只能说明纳粹并不所有都是坏人，左宗棠“就抚回民”只能说明他多少还有点人性，并不足以说明其正义；<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;第四点中，这是一个很聪明的类比推理，关键其实不在是否国家与国家，或者民族与民族的问题，而是“同样是屠杀了无辜，二战时的无差别轰炸为正义，那么左宗棠的屠杀无辜也是正义的”这样一个推理，事实上这条推理还是存在问题的，一方面双方所使用的武器是不一样的，二战时的无差别轰炸是用不能精确制导、杀伤范围较大的高空远程的炸弹，也就是说这种武器基本无法把无辜的人和有罪的人区分出来；而左的时候虽然已经有了火枪火炮的装备，但是依然冷兵器为主要武器，在这种情况下，有罪和无辜完全是可以进行识别的，而左的“无差别”屠杀分明是有违情理国法的，更别说是否正义了；<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;第五点中,为夺回土地而屠杀无辜肯定是非正义的,这就如同经济罪被判处死刑一样缺乏基本的伦理基础；<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;第六点中，事实上“维护中华民族完整”的这句话是不完整的，屠杀从本质上来讲是属于破坏完整的行为的；况且当时的陕甘回变并没有建国诉求更多的是以暴乱抗击朝廷的民族歧视政策的；更何况&quot;中华民族&quot;这个词是从梁启超是才开始用的，对于左宗棠来说更多的是实现朝廷的意图：以军事手段使陕甘恢复暴乱前的有序状态，重新建立朝廷的有效统治。而在此目的下的左的过渡杀戮完全已经失去了做为调解者的中立立场，没有了公平可言，罪已超刑，何来正义而言。<br />
&nbsp; &nbsp;&nbsp; &nbsp;总而言之，楼主入戏太深，左宗棠杀戮太重，要论正义，左宗棠及其湘军在合法的情况（比如战场上）处死有罪的暴民的部分是正义的，而其过度屠杀以及屠杀无辜的部分当然非正义。<br />
