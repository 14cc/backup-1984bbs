													<h2>如何评价左宗棠在平定“回乱”时期的功过</h2>
						
						
																											<div id="postmessage_479843" class="t_msgfont">到现在许多人在评论同治光绪年间左宗棠对那些造反回民的惩戒太严厉了，我想说一下，我们不要过于抱怨左宗棠对回逆的杀戮，在那个种族仇杀的年代，当你看见你的同胞姐妹遭到异族人的杀戮，任何一个有血性的男儿怎能会冷静？怎能还讲究“文明准则”？<br />
　　<br />
　　左宗棠是杀戮一些无辜回民，但是这点要明确一下，是回民全族高度参与屠杀无辜汉民千万在先。左宗棠只是在激愤之下没有去约束属下和百姓的报复而已。试想，当异族对你们没有手下留情滥杀无辜的时候，任何一个热爱自己民族的血性之人怎能还去迂腐地讲究什么礼仪？文明？<br />
　　<br />
　　我不认为左宗棠是一个极端民族主义者，他所做的不过是适当惩戒暴乱的回回暴民而已。毕竟，他不是希特勒，他还算安置了那批“反正”、“就抚”的回回而不是灭绝他们。给今天的回民留了种。<br />
　　<br />
　　回顾历史我们可以看一下，那些西方民主国家怎么样？――进入了20世纪，这些所谓文明人依然有血腥的杀戮。二次世界大战中，邱吉尔先生针对纳粹德国的大轰炸，对德国人实行了疯狂地报复――实行大规模的“无差别轰炸”。整个二战期间，英国只有5万人被德国炸死，而德国却在英美联军的疯狂报复下被炸死了100万无辜的和平居民。再说另一个受到共产主义熏陶的国家――苏联，这个二战时期死亡近3000万人的国家，当他们杀进德国的时候实行了同样的“纳粹行为”――大批德国居民被杀害，妇女被强奸……<br />
　　<br />
　　来看看吧，20世纪的民主国家英美和思想觉悟很高的共产主义国家俄国，他们何尝宽恕过纳粹德国的罪过？他们屠杀了不下几百万无辜的德国居民，这些国家似乎也并没有贯彻他们所说的“德国人民与希特勒纳粹是不同的”、“不能将少数人的罪过强加给所有无辜的德国人民”……<br />
　　<br />
　　是的，这些工业时代的文明国家当自己掌管战败国的命运时候，他们依然无法做到用文明的方式去对待“犯过错误”的国家。在这些战胜国看来，纳粹德国的人民既然当初支持了希特勒就应该为他们的错误付出必要的代价――只不过，这些“战胜国”不会公开他们的想法而是用实际行动去惩罚“错误的德国”而已（确实很虚伪）。<br />
　　<br />
　　相比于以上的文明的工业国家，我们凭什么去指责左宗棠大人的愤怒呢？难道一个人不该有愤怒吗？不应该有血性吗？要在被屠杀的同胞尸体面前做出一幅宽容大度的样子吗？难道那些犯罪的暴民不该受到惩罚吗？这自然又涉及到一个“原罪”的问题，如果不惩戒暴民，只追究极少数领头者的话，那么会出现一个这样的情况：即同治回乱的暴民会因屠杀而获益的局面。在朝廷大军来到之前，陕甘的汉民已经被杀戮殆尽，他们的土地已经被高度参与“圣战”的回回暴民瓜分完了。<br />
　　<br />
　　面对这样的局面，左宗棠也很难办，不屠杀那些暴民既对不起死者，也无法夺回被暴民霸占的汉族土地。那些回回暴民是不会甘心情愿把自己辛苦杀戮无辜汉族群众的地盘再良心发现地退还给汉族的――我不认为手宗教意识熏陶的回回有这个反省意识。<br />
　　<br />
　　因此，我们不难解释左宗棠对回回的屠杀，原因有几个：1，既然当初你们全民参与屠杀无辜群众的暴行，那么你们就应该为自己当初的选择付出代价（就如同德国选民也必须为当年支持希特勒付出代价一样）；2，不杀戮暴民就无法夺回汉族被强去的土地，否则回回会在这片新“征服”的肥沃土地上繁衍出更多的回回，整个西北――汉人祖先的龙兴之地就会完全被伊斯兰化，而这对这片土地原来的主人汉人公平吗？显然，我们无法否认追求公平有时难免也少不了必要的杀戮。<br />
　　<br />
　　最后，我想说一下左宗棠在“同治回乱”的行为是正义的，这是无可厚非的――他维护了中华民族的完整，惩戒了暴民，使他们在将来造反时不得不顾忌一下那段血腥的历史，这也为西北后来总体安宁奠定了基础（所以历史要连贯地而不是割裂地看）。<br />
　　<br />
　　总之，虽然我们也正视在惩戒暴民的行动中确实有杀戮无辜回民的情况。但是这场平乱的意义仿如整个二战的性质一样，作为胜利一方的美国、英国和苏联在对待战败者德国和日本时尽管也有一系列显然是有组织有目的地屠杀无辜良民的行为，但是我们不能因此而否定这场战争的正义性。<br />
　　<br />
　　对于战争，只要把握其总体正义性就可以了，至于具体环节上难免会出现报复和种族灭绝。作为胜利和失败的双方都应该反思而不是一味地去抱怨和指责。如此，则世界才太平许多。<br />
　　<br />
　　对于野蛮，有时只有更野蛮才能使你的对手复归于冷静――这是我们这个社会无奈的生存法则，我们必须面对而已。<br />
　　<br />
　　历史也有相似答案：<br />
　　<br />
　　汉武帝时，博士狄山认为在和匈奴的关系以“和亲”最为有利，主张对匈奴人施“仁义”，应该去教化匈奴人。他以为：匈奴的残暴源于不识礼仪。因此，应该用儒家的一套去感化匈奴人。汉武帝问张汤，张汤说：“这是儒生的愚蠢无知。”狄山却强辩：“我虽然愚蠢，但去愚忠，像张烫那样是诈忠。”汉武帝不悦，对狄山：“让你管理一个郡，能否保证无盗贼？”狄山对曰：“不能。”汉武帝再问：“管理一个县呢？”狄山回答：“不能。”汉武帝追问：“管理一个山头若何？”狄山思衬，在回答不可以就要被治罪了，于是勉强回答：“可以。”于是汉武帝派狄山去山上防御匈奴。结果不出一个月，狄山就被匈奴杀了。这个主张以礼仪教化匈奴的腐儒最终死于野蛮人的手下。<br />
<br />
以为左宗棠尽管确实有屠杀回民的行为（这点我们可比那些“护短”、不反省的某教教众强），但是他仍然不乏人格魅力，算是一代大儒。<br />
　　<br />
