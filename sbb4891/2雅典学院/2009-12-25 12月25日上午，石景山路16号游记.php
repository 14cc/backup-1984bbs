													<h2>12月25日上午，石景山路16号游记</h2>
						
						
																											<div id="postmessage_395202" class="t_msgfont">转帖：<br />
明儿一早九点半一中院，有人一起去打酱油吗？<br />
平安夜晚上给几个人群发了短信，大多有事或嫌太早。很久没有听见那么大的风声，大家纷纷回复，要当心，等你消息。<br />
不管去或不去，至少都在关心。<br />
<br />
早上出门的时候，真的冻得腿都没感觉了，这是第一个在北方的冬天，google显示室外温度-10度。<br />
吃了两个鸡蛋灌饼后一路地铁到八宝山──光是A和D两个出口的警察就有至少不下10个。<br />
D 出口出来，一路跟随路边警车的指引走到石景山路16号，一中院正门。警戒线拉在围墙5-10米开外，站在门口探头探脑一会儿，一个便衣过来问：姑娘，来干啥。我答：随便溜溜。他叫我上其他地方溜溜。正门处人很少，基本都是警察和便衣，于是索性继续往西走，沿着警戒线一路到鲁谷大街，警察越来越多，预感走对了地方。<br />
现在再在google地图上查，发现鲁谷大街上的小楼叫“立案楼”。在离立案楼大约50米处被警察叔叔拦下，要检查身份证。我说那你也给我看你的证件，他出示证件后，我想，礼尚往来呗，没想到身份证直接被收走。随后就是“你为什么过来”诸如此类的愚蠢问题，拉锯了将近10分钟。<br />
问答内容大致如下──<br />
来干嘛？<br />
来围观。<br />
围观什么？<br />
你们其实知道的呀，干嘛还问咧？<br />
你看不到的。<br />
那我就看你们吧。<br />
这个时候有俩外国人长驱直入，我问他们为什么他们能过去我不能。对曰：就像老板让你8点上班难道你偏要8点半上班吗？简直很想膜拜这帮人的逻辑…<br />
我一直在关注自己身份证的去向，倒也没被拿远，那警察走了两步，两三个人上前一同研究该证。<br />
过一会儿，一个稍微年长的便衣把证拿来还我，比较和蔼地说了一句“那么冷的天，何必呢”。<br />
我也笑笑，回他一句“你们也一样冷，何必呢”。<br />
这个时候一个警号043929的年轻警察怒气冲冲过来，嗓门很响道：谁跟你嬉皮笑脸呢！谁跟你抬杠呢！还不走想干嘛呢！<br />
果然长江后浪推前浪。跟这种人争也没什么意思，横竖过不去，就退到了马路对面。<br />
才站一会儿，又有小伙儿上前对我说：姑娘，叫啥名字？<br />
看样子也是个便衣，但还算面善，就跟他开玩笑说：你…这是在搭讪我？<br />
丫被雷到。<br />
过一会儿，一个高个子大汉在簇拥下朝这个方向走来，小便衣对我说，你就走过去一点儿吧，领导来了我该挨骂了。<br />
好吧，与人方便与己方便，再远个10米也没啥。警察也是事先划定了戒严区域，一走过小便衣指示的工地口，站多久都没人过来盘问了。<br />
<br />
来得有点早，9:15分。街对面的小门口拥满了境外记者，也不容易，圣诞节本该放假。<br />
开始冷得有点儿发抖，人很少，有个男生一开始在身边不远处啃着汉堡观望，随后绕到别处去了，猜测可能是推友。<br />
<br />
后来发生的事情有点儿温暖。<br />
9:40分，一个女子对我说，帽子真漂亮，我有个和你差不多大的女儿。<br />
不知怎么就有亲近感，两个人用很少的言语互相试探。在等结果？里面是你朋友？我们等的恐怕是同一个结果吧。<br />
我一边跺脚一边说，真冷。她说，偏偏是今天那么冷，冷得让人记住。<br />
并没有互问姓名，将近10点，她对我说，走，请你去喝点东西。<br />
始终没有消息的情况下，这或许是最好的选择。小门对面就是通往万达广场的台阶，拾级而上，发现还有统筹全局者在高处拿着望远镜。<br />
到KFC坐下以后，打电话给姚遥，问他知不知道最新情况，答曰不知。<br />
放下电话，女子，或者应该叫阿姨问我，姚立法的儿子？很有意思，一步步确信彼此是同道中人的感觉。<br />
姚遥说，顶多也就是5年吧。我这么复述了，然后，她突然拿起手机，说，11年。<br />
门外有扛着摄像机的老外走过，我迅速跑过去询问并得到证实。<br />
两人一时相对无语。发了一条推，阿姨问我推上的id。我便也问她，她说，我用的是实名。<br />
那，能告诉我不？<br />
崔卫平。<br />
<br />
一瞬间有点空白，她拍拍我的额头，笑得很灿烂。<br />
她说，30年前魏京生判了15年，30年后刘晓波判了11年，我们用30年的时间，换来4年的进步。<br />
她说，生活还是要继续，该干什么还是干什么，只是有时候，普通的日子会有一个断裂，譬如今天。<br />
她说，昨天打了很多电话给老朋友，其实不想逼别人面对自己的良心，但有时候，我们需要确认最原始的善意。<br />
她说，不要搞得苦大仇深的好像需要别人来救我们似的，我们不阳光谁阳光？<br />
<br />
坐在KFC里、走去地铁站、从八宝山到复兴门，一路有一搭没一搭地聊了很多。几乎都是她讲我听，唯一能贩卖的只是曾经大学老师的一些话，和前些日子同《中国农民调查》作者夫妇的聊天所得。<br />
在一中院门口，她也被盘查，警察叫她走开，她便走开了。“我们服法，但不认罪”。到底比和警察抬杠调笑的我更有态度得多。<br />
断裂过后还是要重回轨道，车到复兴门，道一声崔老师再见，她说，祝你快乐。<br />
离家不远的超市里，买了羊肉、萝卜、鸡蛋、酸奶、花卷……我们没有理由不好好过。</div>

							
							
							
							
