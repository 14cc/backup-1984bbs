																											<div id="postmessage_644218" class="t_msgfont">菲利普斯的理论叫做【维特效应】【Werther Effect】即自杀模仿现象。所谓「维特效应」指的是两百年前德国大文豪歌德发表了一部小说，名叫《少年维特之烦恼》，该小说有着异常强烈的时代精神，它所提出的问题带有时代的普遍启蒙意义。对于维特的精神和性格，诸位定将产生钦慕与爱怜；对于他的命运，诸位都不免一洒自己的同情泪。小说发表后，造成极大的轰动，不但使歌德名声在欧洲大噪，而且在整个欧洲引发了模仿维特自杀的风潮，为此，好几个国家将《少年维特之烦恼》列为禁书。<br />
菲利普斯研究兴趣之一就是追踪从那时开始一直到现代所出现的各种形式的「维特效应」。他发现对轰动性的自杀事件报道，在报道所涵盖的地区，紧接著自杀率就会有大幅度上升。因此他得出这样的结论：一些内心痛苦的人，看到别人自杀身亡消息後，就启发了自杀念头，效仿了自杀者。这里面贯穿著一个社会认同原理，内心痛苦的人看到其他内心痛苦的人采取了自杀的形式，他也就认同了这种消除痛苦的手段。<br />
　　菲利普斯是通过对一九四七年到一九六八年之间美国自杀事件的统计得到「维特效应」证据的。他发现每次轰动性自杀新闻报道後的两个月内，自杀的平均人数比平时多了五十八个。因此从某种意义上来说，每一次对自杀事件的报道，都杀死了五十八个本来可以继续活下去的人。菲利普斯同时发现，自杀诱发自杀的现象主要发生在对自杀事件广为宣传的地区。而且，这种宣传越是广泛，随後的自杀者就越多。<br />
　　例如,在媒体报道了玛丽莲梦露的自杀新闻之后,那一年全世界的自杀率是增长了10%；最近，深圳富士康的连续跳楼事件，也可以印证这一点。</div>

							
							
							
							
