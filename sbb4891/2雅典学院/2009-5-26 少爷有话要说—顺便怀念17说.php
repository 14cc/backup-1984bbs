<br />
<br />
【何为民意】<br />
&nbsp; &nbsp; 少爷认为，中国人的民意就是女人的卫生棉，有事了掏出来糊弄几天，事过去了，就连同那摊子污血一同抛到眼不见的角落了。咱们的皇帝开口必是：“朕贵为天子，啊，啥啥啥的”，下面跪着的文武百官马上一呼百应：“此乃天意啊，啥啥啥的！”<br />
&nbsp; &nbsp; 你看，民意在哪呢？<br />
&nbsp; &nbsp; 但，不是说，屁民们就没有自己的想法和意识。“谁做皇帝老百姓都是穿衣吃饭”、“谁让老百姓有饭吃，就让谁当皇帝”，这就是中国的民意。所以外族不外族的，或是谁当皇帝了，屁民一律不关心，他们只关心自己的收成，自己的饭碗。<br />
&nbsp; &nbsp; 几千年来往复折腾的，其实也就是这档事儿---吃饭拉屎!<br />
&nbsp; &nbsp;&nbsp; &nbsp; 所以，千万别以为自己能代表得了“民意”，我们是另类，是一小撮，至少目前是。<br />
<br />
【网民的力量】<br />
&nbsp; &nbsp; 有人说网民有3多个亿呢，据少爷观察，这个数字有待考证，不是对捅妓菊不信任，而是只要沾上免费两字，中国人难免不会一拥而上，你说天涯著名马甲---斯巴达三百勇士算几个网民？<br />
&nbsp; &nbsp; 少爷再观察一下，即便是3个亿里，至少2亿人在玩游戏看电影，剩下的1个亿忙着偷菜买奴隶。真正关心时政的，是整数后面那个零头，其中大多是毛派、网评○，然后是我们这些另类。自己看看，全国论坛有多少？猫眼天涯有几个？<br />
&nbsp; &nbsp; 所以，我们还是一小撮。<br />
&nbsp; &nbsp; 不要把网络反腐看的太重，那只是当权者党同伐异的一个催化剂。没站错队的，任你咋折腾都稳定着呢，站错了的，不用你监督他也照样挨收拾。<br />
&nbsp; &nbsp; 网络，是一种即时通讯手段，可以呼吁、可以分享，但是缺少对现实恶行的惩治，这点必须认清。<br />
<br />
【谁是五毛】<br />
&nbsp; &nbsp; 各位可以随意指认他人甚至少爷我是五毛，但是谁收了酬劳谁自己心明白。俺可以说，至今为止，没人付过我一分钱，俺上网打字纯属自娱自乐。俺也不轻易说别人是五毛，我没证据。<br />
&nbsp; &nbsp; 最过分的评说，我也只能说你是个异见者。<br />
<br />
【被拯救还是自我救赎】<br />
&nbsp; &nbsp; 少爷我想问，你是愿意先把环境卫生打扫好了再请客人上门，还是等着客人来给你打扫？<br />
&nbsp; &nbsp; 虽说美国人天生有着救世情节，但也没义务来替中国人清理那坨污秽吧。<br />
&nbsp; &nbsp; 毕竟，那坨东西再怎么作恶多端，也没去扒美国佬的房子，没去嫖宿人家的女儿。<br />
&nbsp; &nbsp; 战争是要死人的，怎么好意思让人家美国捞抛家舍业的流血牺牲，中国人坐享其成？不觉得自私么？不觉得卑劣么？不觉得羞愧么？<br />
&nbsp; &nbsp; 所以，名正言顺的去自救，别等着人家施舍给你一点点怜悯。<br />
&nbsp; &nbsp; 如果艳羡人家的经验，请先自修其舍，清理干净，再邀请人家来传经授典。<br />
<br />
【这个民族值得拯救么】<br />
&nbsp; &nbsp; 少爷评论这个民族，最常讲的一句话就是“我们是一个退化到只会吃饭拉屎、交配繁殖、下跪磕头的可悲民族”。这就是一种文明的倒退，生物史的退化，让人打心眼里鄙夷！&nbsp; &nbsp;<br />
&nbsp; &nbsp; 过去的一个世纪里，西方各国经历了政治、经济、文化上的飞跃之后，大洋彼岸的文明社会，开始追溯反思曾经的文明为何消亡。进而转向伦理和人性的进化。而这一百年来，我们这个民族如何了？<br />
&nbsp; &nbsp; 面对权贵和强暴，萎懦下跪，磕头捣蒜。――血性何在？<br />
&nbsp; &nbsp; 兄弟邻里之间一言不合恶目相向，拔刀以对。――这时他们的血气又方刚了！<br />
&nbsp; &nbsp; 民众之间相互践踏，相互欺骗，造假贩假已成风行。――谎言成性的民族！<br />
&nbsp; &nbsp; 最后闹的连外国人都看不下去了，但人家没好意思这么说，很客气的讲了一句：跟一百年前比没什么进步！于是这个民族就不高兴了，乌泱泱的去抵制，个别地区又砸又抢的，还趁机摸一把占点小便宜。<br />
