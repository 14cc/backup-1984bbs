													<h2>根除刑讯逼供 必有副作用</h2>
						
						
																											<div id="postmessage_627617" class="t_msgfont">转帖：<br />
只有正视法律的缺陷，两害相权取其轻，坦然接受法律的副作用，才能真正地根除刑讯逼供<br />
<br />
近日，赵作海一案再次引发公众的关注，刑讯逼供！还是刑讯逼供！！人们不禁要问，有没有什么办法彻底根除刑讯逼供使得无辜民众不再蒙冤？<br />
<br />
办法是有，当然这个办法不是处理几个打人的警察，惩处几个不负责任的法官那么简单，不从制度上进行改良，处理再多的人也无济于事。<br />
<br />
彻底根除刑讯逼供只有一个办法，那就是让嫌犯的口供不再是合法的证据，换句话说就是赋予嫌犯沉默权。<br />
<br />
我们经常能在美国的影视剧中看到，每当警察抓到嫌犯时，第一件事就是要像和尚念经一样背诵着“你有权保持沉默，否则你所说的一切，都能够而且将会在法庭上作为指控你的不利证据……”，没错这就是著名的米兰达规则。很多人了解美国的沉默权，往往是从这个米兰达规则上开始，很多人误以为米兰达规则规定了嫌犯的沉默权。其实，沉默权是美国宪法第5条修证案规定的“任何人不得在任何刑事案件中被迫自证其罪”，米兰达规则只不过是使得沉默权得以延伸，认为公民不自证其罪的权利不仅适用于正式法庭审判，而且同样适用于法庭以外的任何程序和场合。由于涉案警官在审讯米兰达之前，没有预先告诉他应享有的权利，所以米兰达的供词属于非自愿供词，这种供词在法院审判时一概无效。<br />
<br />
米兰达规则实施以后，其作用是积极的也是明显的，从此之后，再也没有哪个嫌犯因受到刑讯逼供而蒙冤入狱，美国的司法系统也会办错案件，但错的原因没有一件是因为刑讯逼供，可以说赋予嫌犯沉默权，使得口供不再是合法证据是根除刑讯逼供的一剂良药。<br />
<br />
但是，天下没有免费的午餐，实行米兰达告诫这种保护嫌犯权利的法律，是有重大的社会代价的。<br />
<br />
米兰达告诫实行以后，嫌犯一旦落网，一个个都像榆木疙瘩一样一声不吭，给警方侦破案件造成了极大的困难，更要命的是，有的警官好不容易抓到嫌犯后，死活想不全米兰达告诫全文了（我数了一下，全文共61个英文单词），不得不把嫌犯放掉，后来，警方干脆把米兰达告诫印成卡片发给每一位警官，在抓获嫌犯后，照本宣科交差。这样带来的后果是，警方的破案率直线下降，据美国司法部门统计，米兰达告诫出台前，刑事重案破案一般在60%左右，“米兰达”告诫出台后的几十年，破案率已跌落在40%左右。<br />
<br />
这也就是说，要想加大保护嫌犯权利的力度，必然要对警方的权限予以约束，也会加大司法成本，更会使得更多的罪犯逃脱法律的制裁。刑讯逼供虽然会使无辜民众蒙受不白之冤，但我们也不得不承认，它在打击犯罪方面有着惊人的高效率作用，许多案件警方是依据嫌犯的口供侦破的。对米兰达规则持反对意见的美国联邦大法官哈兰认为：“人人皆知，如果没有嫌犯口供，有些案件很可能永远无法破案，数量众多的专家作证表明，警方提审案犯，获取口供，对于控制犯罪非常重要。”<br />
<br />
当然，有人会说，有没有更好的办法，使得警方高效率打击犯罪，又不滥用权利伤害无辜民众呢？比如说，并不赋予嫌犯以沉默权，口供仍是合法证据，但嫌犯是打不得的。这个想法是美好的，当今世界上没有哪个国家认为刑讯逼供的口供是合法的，无论它是不是法治国家。我国有刑诉法也同样如此，虽然没有规定沉默权，没有规定嫌犯问不得，但也同样规定嫌犯是打不得的。但为什么刑讯逼供会如此严重？这还是因为口供在刑事案件侦破过程中的高价值作用，许多严重刑事案件，离开了嫌犯的口供就无法侦破，离开了嫌犯的口供就无法对嫌犯定罪，口供如此重要，足以诱使警方铤而走险，明知犯法，仍会对嫌犯进行刑讯和精神恐吓。实话实说，明显的肉体刑讯现在并不多见，但是车轮式的不间断提审――熬鹰式刑讯却比比皆是，这就足以让嫌犯精神崩溃，真正的罪犯，会不打自招，无辜的人当然也会自认其罪。<br />
<br />
笔者请一位从事刑事审判的法官同行作了一个粗略的统计，在他办理的案件中，如果离开了口供，有多少案件将无法对被告人定罪，结论是百分之七十左右。而且越是重大案件，这个比例越高。<br />
<br />
那么，在我们面前只有两条道路可走：<br />
<br />
一，想彻底根除刑讯逼供，只有赋予嫌犯以沉默权，使得口供没有丝毫的利用价值，其副作用是刑事案件侦破率大幅下降。众多的罪犯逃脱了法律的制裁。<br />
<br />
二，乱世用重典，打击犯罪应当从重从快，现在犯罪率如此居高不下，更不能束缚警察的手脚。其副作用是必然有部分嫌犯会受到刑讯逼供，当然这其中大部分是坏人是真正的罪犯，但也极少部分无辜的人会蒙受不白之冤。<br />
<br />
以上，我讲了为什么我国刑讯逼供这样严重，其主要原因是现行刑诉法将嫌犯的口供列为合法证据，刑事案件的侦破严重依赖于嫌犯的口供，同时，嫌犯的权利没有得到充分的保护。<br />
<br />
最好的刑事政策是，绝不错杀一个好人，但也绝不放不过一个坏人，这个说起来是那么容易，但做起来就绝无可能。要么是宁可错杀一千，绝不放过一个，要么是宁可放过一千，绝不冤枉一个。鱼与熊掌不可兼得，这个道理古人都懂，但真要你做出选择，却是很难很难的。<br />
<br />
还好，愚笨的美国人往往会做出一些令中国人费解的选择，对于米兰达告诫同样如此，最初美国人并不能普遍接受，曾有议员试图提出宪法修正案，试图推翻米兰达告诫，最终是因难以取得参众两院三分之二多数而夭折。但是现在，米兰达告诫已被公众普遍接受，而且实际上已经成为当代美国通俗文化的一个重要组成部分。<br />
<br />
美国人能够接受米兰达告诫，能够忍受米兰达规则所带来的副作用，是因为他们认为，法律首先要管的，应当是政府里的官儿和警察，其次才是社会上的犯罪分子，所谓的加强法治，首先应当从制度上约束和限制政府官员的权力，从制度上防止统治者和执法者无法无天，胡作非为。因为执法者一旦胡作非为，其对社会的危害要比任何的个人犯罪严重的多。<br />
<br />
美国人实践检验这个观点的机会并不多，倒是我们国家在不断地证明美国人这个观点的证确性，从1957年的反右扩大化到文革十年，哪一个不是使数以千万计的民众陷入如山的冤假错案和政治迫害之中？<br />
<br />
两害相权取其轻，这个道理人人都懂，但是，选择起来却不容易，对于刑讯逼供，现在人们看到了其巨大的危害性，几乎人人对于警方的滥用权力侵害无辜公民的行为感到无法容忍，但是，赵作海等案是在真相大白以后才引起人们的关注的，假如赵作海案发于今日，警方遵纪守法，案子破不了，只抓到了一个证据不足的嫌犯赵作海，估计大多数现在骂警方的人会要求警方将赵作海绳之以法，或者假如警方将赵作海起诉到了法院，法院以警方对赵作海刑讯逼供为由将赵作海无罪释放，你能接受吗？<br />
<br />
当然了，你会说，我没有那么笨，你会勉强说你能接受，但这是在你知道了案件真相前提下做出的。算不得数。<br />
<br />
曾经有一个案件，可以验证你的真实想法，东北黑社会刘涌案，刘涌一审被判死刑，但刘涌请了一个好律师田文昌，田文昌律师找到了曾经看护刘涌的几个退役武警，证明刘涌被关押期间曾经受到了刑讯，（李庄曾想复制这一作法），二审法院胆子不大，没敢宣布这些刑讯逼供的证据违法，只是说证据不可靠，因此改判刘涌死缓，这下可了不得了，全国民众大骂二审法院混蛋。最高法院着了急，提审该案，将刘涌枪毙了事。<br />
<br />
现在大骂刑讯逼供的人，和当初大骂刘涌案的人难道不是一伙人吗？<br />
<br />
