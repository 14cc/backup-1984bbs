<br />
其实，我从来就不否认楼上诸位没有看完帖就骂我的人，以及当初骂刘涌案的法官的人，还有现在骂赵作海的警方的人，都是一群善良的人。他们的善良使得他们相信世界上确实有十分完美，没有任何缺陷的法律制度。正如他们相信世上有真神一样，相信多快好省大干这类口号，也相信我国的不放过一个坏人，也不冤枉一个好人的刑事政策。<br />
