													<h2>周恩来临终忏悔</h2>
						
						
																											<div id="postmessage_96737" class="t_msgfont">2006年1月8日，周恩来逝世30周年。中央党校、中央理论研究室、中央党史研究室，曾召开“周恩来思想研讨会”，开了四个下午。会上首次公开了邓颖超日记档案、周恩来病危期间与邓小平、叶剑英的谈话档案。这一切，可以从邓颖超日记中得到印证。 <br />
邓颖超日记<br />
&nbsp; &nbsp; 1976年10月，邓颖超就自己的日记，向中央政治局请示：“如何处理有关资料档案？”叶剑英代表中央政治局常委会告知邓颖超：“现在工作繁多，也很复杂，还是你保管好。”<br />
&nbsp; &nbsp; 1981年7月2日，在中共十一届六中全会后，邓颖超又提出有关资料问题。中共中央主席胡.耀.邦，代表中央政治局常委会，对邓颖超说：“常委和部分政治局委员都看过，还是由你保存比较合适，考虑到多个方面：党内团结、党的形象、毛主席功过七三开评价、人民对总理热爱等方面。”直至邓颖超逝世后，她的日记一直由中央政治局属下的机要局保管。 <br />
&nbsp; &nbsp; 2004年3月，中共对邓颖超日记作了启封，有限范围作党史研讨，不作政治结论。<br />
周恩来病危期间，毛泽东拒往探视<br />
&nbsp; &nbsp; 1975年1月24日，周恩来病情恶化住院，直至1976年1月8日逝世。在这一年中，中央医疗小组先后在5月7日、7月11日、9月25日、11月12日、次年1月5日发出五次病危通知。<br />
&nbsp; &nbsp; 1975年5月3日，毛泽东召开中央政治局会议。会上，叶剑英、陈锡联提出：“主席能否安排到医院探望一下周恩来？”毛泽东迟疑一下说：“我可不是大夫。”<br />
&nbsp; &nbsp; 1975年9月28日，在第三次发出周恩来病危通知后，叶剑英、李德生、朱德、李先念、许世友、陈锡联、韦国清等联署上书毛泽东：“盼毛主席能到医院看望自己五十年的战友。”毛泽东委托毛远新传话：“我从不勉强别人，也不希望别人来勉强自己。”<br />
&nbsp; &nbsp; 1976年1月8日，周恩来逝世。12日，中央政治局请示毛泽东的意见：“主席是否出席追悼会，是否由邓小平致悼词？”毛泽东口述指示：“不要唯心，也不要勉强”，“邓致悼词恰当”。<br />
病危期间周对邓颖超嘱咐“三不要”<br />
&nbsp; &nbsp; 以下是邓颖超日记中，有关周恩来病危期间与邓颖超谈话的部分摘录：<br />
&nbsp; &nbsp; 1975年5月10日：“小超，我百思不解的是：斗争没完没了地搞下去，马克思哲学是一部斗争哲学吗？斗谁，和谁斗？”“怎么会造成今天的局面？”<br />
&nbsp; &nbsp; 1975年9月12日：“我快走了，快了。走后，一不要过问政治；二不要留在中南海；三不要留在北京，回老家养病、休息。记住，记住了，我也可放下些心。”“这还叫人民作主的共和国？”<br />
&nbsp; &nbsp; 1975年10月3日：“我常在总结自己走过的道路。我坚信马克思主义道路，坚信共产主义是人类奋斗理想的目标。建国二十六年了，政治斗争一个接一个，这样下去，把国家带到灾难境地，这还叫社会主义社会，还叫人民当家作主的人民共和国？我的一生还留著书生气、失望走向归宿。”<br />
周恩来道出遵义会议的真相<br />
&nbsp; &nbsp; 1975年11月15日：“我想在生命最后时期，还是要自我反思、检讨、澄清若干事件。尽管是晚了，但总不能让其错、假继续下去。历史谁也篡改不了。1935年1月，遵义会议上，是确立了张闻天同志为代表的党中央，是中国共产党历史上一个生死攸关的转折点，要把歪曲的历史更正过来，好在当年参加会议的同志还在。”<br />
周后悔给毛抬轿子<br />
&nbsp; &nbsp; 1975年11月17日：“1944年5月21日，中共六届七中全会第一次会议，选出朱德、刘少奇、任弼时、周恩来组成主席团会议，有通过决议：得票最高者为主席团主席。刘少奇最高，朱德第二，毛泽东是第四。但是在内部由我提议：主席还是由毛泽东来担任。朱老总是很反感的。我又一次做了唯心的政治上错误的抉择。”<br />
周成全了毛的终身领袖地位<br />
&nbsp; &nbsp; 1975年11月19日：“1962年1月．中共中央召开扩大工作会议（编者按：即七千人会议）。会上总结人祸带来灾难的教训，强调要恢复党的实事求是、群众路线、健全党内民主生活。会上有不少同志提出：主席退下。在二月十日的政治局常委会上，毛泽东表示：愿服从会议决定，辞去主席退下，搞社会调查。朱老总、陈云、小平表态：欢迎毛泽东辞去主席。是我坚持：主席暂退二线，主席还是主席。”<br />
&nbsp; &nbsp; 1975年11月22日：“1956年9月29日，中共八届全会后第一次政治局会议上，通过二项决议：党的主席规定连任一届；要限制领导人权力，加强对领导人的监督，党内要体现民主集中制。是林伯渠、罗荣桓、彭真提议的。十七名政治局委员，十五名赞成，惟有二人弃权（毛泽东、林彪）。决议都给个人意志废了，我们也有责任和罪过。”<br />
周对毛泽东路线的质疑和否定<br />
&nbsp; &nbsp; 1975年12月3日：“一场政治疾风暴雨要降临，还要斗，斗到何日何时方休呢？共产党哲学是一部斗争哲学吗？社会主义现代化建设是靠斗争能建成的吗？”<br />
&nbsp; &nbsp; 1975年12月28日：“国家很不幸，建国26年，还有6亿人口饭也吃不饱，只会高歌共产党、颂扬领袖，这是共产党败笔。”<br />
周对1976年元旦社论的反应<br />
&nbsp; &nbsp; 1976年1月1日：“不许放屁，内外树敌，国家正陷于经济危机。谁主沉浮？人民，醒悟了的人民。” <br />
&nbsp; &nbsp; 1976年1月2日：“记住：不留骨灰，不建墓碑，要远离中南海。”<br />
