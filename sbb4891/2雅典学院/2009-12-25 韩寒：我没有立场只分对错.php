													<h2>韩寒专访：我没有立场只分对错</h2>
						
						
																											<div id="postmessage_396559" class="t_msgfont">作者：张洁平<br />
<br />
韩寒说，中国人有一个很大的问题就是不分对错只有立场，而他是一个没有立场只分对错的人。他父亲在政府宣传部门工作，但不愿意入党，而且还会经常帮他修改博客上的错别字。&nbsp;&nbsp;<br />
<br />
　　眼前的韩寒，笑容谦和，语气平缓，没有博文中的尖锐，也没有传闻里的腼腆。在全国汽车拉力赛的最后一站福建邵武，亚洲周刊专访了二十七岁的韩寒，听他讲他自己的故事。两天以后，他获得了职业赛车生涯中第一个全国拉力赛总冠军。<br />
<br />
&nbsp; &nbsp; 以下是专访内容：&nbsp;&nbsp;<br />
<br />
　　你每天分别花多少时间在看书、网络和电视上？&nbsp;&nbsp;<br />
<br />
　　看书一个小时，网络两个小时，电视一个小时，主要是看体育。&nbsp;&nbsp;<br />
<br />
　　你有固定的时间写作吗？&nbsp;&nbsp;<br />
<br />
　　因为我国发生的各种奇异新闻事件的时间不是很固定，所以我写作的时间也不是很固定。&nbsp;&nbsp;<br />
<br />
　　这一两年，你开始大量关注公共事件，发声，甚至行动，越来越像一个“公共知识分子”，你自己怎么解释这种变化？&nbsp;&nbsp;<br />
<br />
　　我其实从小就是如此。我只是一个作者，我从小看的书籍都告诉我，一个写作者就应该这样写文章，我一直想说，我的每一篇杂文，其实才是真正的纯文学。而很多人都觉得你是在不务正业。其实，我一直是在务我的正业。而所谓的公共知识分子，其实我并不这样想，我只是单纯的去判断对错而已，我是一个没有立场只分对错的人，中国人有一个很大的问题就是不分对错只有立场。对于我来说，如果政府做的是对的，我也会赞美，只可惜当局给我这样的机会并不多。而且出现这种情况的时候，当局已然夸张地赞美了自己……&nbsp;&nbsp;<br />
<br />
　　许多评论者会因为自己一再批评的事情仍然不断发生而感到无力，你会有这样的无力感吗？&nbsp;&nbsp;<br />
<br />
　　我的无能为力从不体现在这方面。事实上，他们是对自己能够改变的东西抱有太多的期望了。文人就是文人，如果文人能改变什么，那也是一个漫长的过程。而有一天如果文人真的突然迅速能改变了什么，那一定是被某政治利益所相中的文人。&nbsp;&nbsp;<br />
<br />
　　如果把一切想得好玩一些，就不会那么无力了。他们的目标就是应该让这个世界上的人，每一个人都那么的无力，包括总统或者主席，那么这个世界才是好的。&nbsp;&nbsp;<br />
<br />
　　你妥协过吗？什么样的情况你会妥协？&nbsp;&nbsp;<br />
<br />
　　我经常妥协，如果一个人说他永不妥协，我认为他一定演猛男演得入戏太深了。&nbsp;&nbsp;<br />
<br />
　　能说说你上一次去地震灾区的经历吗？那次经历是否对你有很大的触动？如果不是地震的话，近年什么事对你的触动最大？&nbsp;&nbsp;:<br />
<br />
　　事实上很奇怪，汶川地震真的是大灾难，但真的没有给我什么触动。前两天我看到一个新闻，讲一个老汉从六十岁一直蹬三轮车蹬到九十岁，蹬车挣了三十多万，自己住在一个小的棚子里面，捐助各种各样的学生，反而这故事我觉得挺触动的。还有比如一个流浪汉死了，这个流浪汉养的一只金毛，一直守在那个窝里，第二天看到终于有好心的市民把这只金毛给领走了，哦，我觉得心里踏实了……事实上，我更能被具体的事情触动。所以我特别能理解艾未未，他为什么要一个一个名字地去找那些学生。因为那是具体的，真的当一个数字，六万、七万、八万，或者很多很多尸体、倒塌的房子出现在你面前的时候，你反倒好像进入一种士兵到战场上的感觉，可能不会有特别大的触动，或者来不及触动了。这就是为什么我们永远要强调个体，因为个体是最动人的，当所有的人都捆绑成一片的时候，当死亡都变成了数字的时候，就没有办法去触动别人。&nbsp;&nbsp;<br />
<br />
　　你觉得自己这种重视个体、自由自在的态度是从哪里来的？&nbsp;&nbsp;<br />
<br />
　　我想，这种态度反而要感谢当局。在一些政治运动或者学生运动以后，他们选择了淡化很多东西，尤其是信仰。我有一个朋友拍了一部电影，叫《梦想照进现实》，那部电影一点问题都没有，但有个地方总是不能过，是片中两个人聊天的时候聊到“信仰”两个字，电影局说，这不能过，带这两个字就不能过。这说明当局在回避信仰，但它造成了一个很好的结果是没有信仰。我觉得这是很好的一件事，很多人说，你们这代人完了，没有信仰，拜金主义，我说拜金主义也不错啊，虽然不高尚，但安全，至少他们对这个社会无害，对其他人的人生无害，不会像中了邪一样自相残杀。我觉得这才是一个民族慢慢苏醒的开始，太好了，它终于没有信仰了，终于不会再有那些邪恶的、打着信仰名义的各种坏的东西，它才能慢慢创造别的信仰。我认同这条拜金主义和无信仰的道路，它是一切的根源。&nbsp;&nbsp;<br />
<br />
　　你怎么看待你和国家的关系？&nbsp;&nbsp;<br />
<br />
　　我在去年的家乐福事件里写，要把祖国和母亲分清楚。人家老是说我，你无论说的对错，都要讲大是大非，民族大义就是大是大非。我觉得这个太狭隘了。我是特别希望外星人突然来侵略地球一次，这时候他们可能才知道，那是属于人类的东西，而不是属于民族的东西。&nbsp;&nbsp;<br />
<br />
　　民族大义和祖国母亲是我们从小教育里最根深蒂固的东西。我其实尊敬有些人觉得要与国家共存亡的想法，但是很多人把概念模糊了，把国家和政权绑在一起。这也没关系，慢慢来吧，总会想清楚的，当你发现你很穷，房子也买不起，什么也买不起，当你发现明明一些恶人，却过着很好的日子，到最后你想去自杀的时候，还要收你一笔捞尸费，我相信他的在天之灵终于会想明白很多事情。&nbsp;&nbsp;<br />
<br />
　　你怎么看艾未未？&nbsp;&nbsp;<br />
<br />
　　我个人非常喜欢这个大汉，我计划参加他的中指大赛。他和很多所谓的独立分子或者异见分子或者公共知识分子不一样的是他更好玩，心态更好，有恨是正常的，但他心中没有那么多的怨，而且他很有才华。&nbsp;&nbsp;<br />
<br />
　　艾未未因为持续关注地震遇难学生，现在的处境很危险。与他有同样关注的谭作人，半年多前已经入狱，现在仍没有结果。你怎么看他们遇到的状况？&nbsp;&nbsp;<br />
<br />
　　我一直很困惑，他们究竟干什么了？他们没干什么呀……我只能说，当局存在不确定性，他们没有标准。要是他们有很多标准，比如能说这个不能说这个，我完全可以接受，当局如果有一个立法，比如“禁止讨论法”什么的，我现在可以接受的，不跟你开玩笑。你只要把事情放到面上来：哪些是屏蔽词，屏蔽词你不能说，说了你就要承担一个责任，你规定了，那好，我们不说咯。互相给互相一个面子。事实上你是一个什么性质的国家，全世界都知道，归根结底你只要让老百姓过好日子，在现阶段就OK了，什么能讲，什么不能讲，说清楚。但你又不告诉人家标准，人家说了，你要去惩罚人家。这就像以前的流氓罪一样，定义是什么，是他们自己掌握的。&nbsp;&nbsp;<br />
<br />
　　说说你的阅读经历吧，从小读什么书？&nbsp;&nbsp;<br />
<br />
　　我读的书很奇怪，很杂，比如《十万个为什么》、钱钟书、胡适、梁实秋、林语堂，民国时代那批人。那是初中时候，其实那时候看不懂，看不懂他们文章中表达的别的意思，但能看懂他们的文字写得很好，这是现在的所谓的作家所欠缺的。这七八年我已经很少看书了，都在看杂志，各种各样的杂志，而且我只看买得到的杂志。我也不翻墙。&nbsp;&nbsp;<br />
<br />
　　会去读一些理论类的书吗？比如哲学、政治学、社会学？&nbsp;&nbsp;<br />
<br />
　　这类书我完全看不下去的。无论你讲得再有道理、再高深，我就会想到一个像我这样的人勤勤恳恳晚上在那里写，我就一下觉得没有趣味了，就像李敖看到他老婆也会上厕所一样。我觉得书要好看好玩，这是最重要的，否则我没法入戏。我看的时候老想，这个人写的时候他不该这么写，这么写他煽动力就下降了，要换一句话才能忽悠更多的人。你说这种心态怎么看这种东西，无论西方的当代的马克思的列宁的都一样。我也会试着看看我们领导人的文集，看着看着，咦，这句话露怯了，该给他换掉才能更加忽悠得好一点。&nbsp;&nbsp;<br />
<br />
　　是觉得不需要理论？&nbsp;&nbsp;<br />
<br />
　　理论这个东西，就像对上海很多女孩子来说的一套房子一样，它是给人安全感的东西。但我并不需要理论带给我安全感。就算有个事情，让人看不懂、看不清，我觉得大部分时候，即使有理论，一样也是看不懂，看不清的。我是一个车手，我搞不清楚发动机是怎么工作的，但我能把它用得很好，这就行了。&nbsp;&nbsp;<br />
<br />
　　你和你父母沟通多吗？觉得他们了解你吗？&nbsp;&nbsp;<br />
<br />
　　他们还是挺了解我的。我的很多性格受父亲影响。我父亲在政府宣传部门工作，还是党报一版的编辑，他又从不愿开后门，也不愿意入党。他觉得入党没有什么大的意思。我觉得，他的人格也很奇怪（笑），他又在宣传部门工作哦，好像一个卧底。其实很多人在政府里面都是这样的，他就觉得那是工作，把工作完成了就好。&nbsp;&nbsp;<br />
<br />
　　你父亲会看你的博客吗？他们同意你的观点吗？&nbsp;&nbsp;<br />
<br />
　　会看，我把博客密码给了我父亲，他会经常上去帮我修改博客上的错别字。他们跟我没有观点冲突，他们只是偶尔觉得你应该稍微缓和一些。我在老家的邻居是村长，他很多时候不同意我的观点。（村长也看你博客？）他自己不看，但听人说到的时候他会不赞同。但我觉得没问题，赞同不赞同都没问题，最后吵不出个结果的时候，大家洗洗去睡，让时间来说真话。哪怕时间没能说真话，这也只是个讨论，不要到最后，看谁手里有权力，就去迫害另外一方，我觉得这才是最不好的一件事情。哪怕你不同意，你动用所有的舆论力量说这个是错的，动用所有的五毛党，我觉得这些都没有问题，只要不要诽谤你或者伤害你，不要试图去消灭你的声音，甚至消灭你的肉体。&nbsp;&nbsp;<br />
<br />
　　你不介意五毛党？&nbsp;&nbsp;<br />
<br />
　　人家觉得政府很无耻，怎么有这么多网评员啊五毛党啊，相反我从来不觉得这个无耻，我觉得这是被允许的，就像你要让你的朋友来捧场，政府不同的是他们发钱，但这也是理所当然的事情。但不要到最后，你发现说不过人家，就什么国新办啊、中宣部啊都出动了，这就难看了。最后全国上下只剩下一种声音，空气里飘散着幽怨和悲壮的回响，这个真没有必要，他们完全可以做得更好。他们很多部门还是太老了，办事的方式、手法停留在二三十年之前。他们的公关太差。&nbsp;&nbsp;<br />
<br />
　　你的小说男主角，往往是小镇少年。这是不是你对自己的身份映照？许多地方的乡村在慢慢沦陷，你的老家呢？&nbsp;&nbsp;<br />
<br />
　　因为中国的都市反而很土，而小镇更有情怀，更适合小说。我的乡村也在沦陷，当地政府为了收益，只能卖地，卖了地以后的企业又都是化工企业，环境破坏非常严重，而且那些企业经济状况不好，有相当一部分都是巨大的烂尾工程，税收又差，只能继续卖地。最近政府在打我老家附近这块地的主意，对于我本人来说，这种完全不是造福社会的产业，我是不会接受政府的任何条件让我搬迁的。如果要对我的老家进行强制拆迁，我会暴力抵抗到底。届时欢迎海内外媒体和我的读者朋友前来观看，管盒饭。&nbsp;&nbsp;<br />
<br />
　　政府有人来跟你们谈过？他们会顾及你的舆论影响力吗？&nbsp;&nbsp;<br />
<br />
　　四五年前就有这个说法了，他们说过，如果做不通我的工作，会做我父母的工作，做不通父母的，会做他们单位的。所以后来我自己有了一定的收入以后，就让我的父亲母亲都提早退休了。前两天又有人来考察过，我也给镇长写过信。你说影响力，事实上很多东西，我发现完全是两套不同的话语系统，你做新闻、或者做舆论的人可能觉得这个人挺有名气的。但是在他们那里，系统完全是断裂的。有一次我去上海别的区，很多小领导喜欢附庸风雅嘛，他们会握着你的手说哎呀听说你是个作家，你赶紧写些文章帮我们推动一下我们当地的经济发展啊，我听了开玩笑，说好，那我写写你们政府大楼盖得多风光，他们说好好好……你看，是完全断裂的。&nbsp;&nbsp;<br />
<br />
　　官员与社会脱节&nbsp;&nbsp;<br />
<br />
　　我一个朋友的父亲是个小官员，他学会了上网，因为想要了解网上“舆情”，但他上了半年的网，才知道这个世界上除了他们区政府的官网，原来还有别的网站……所以好多事情，你感觉舆论压力已经很大了，政府却完全没有反应，有的事情你觉得没有什么，对方却会当做一个天大的事情来处理。当局就有点像张艺谋的团队，他很想与时俱进，特别想了解这些年轻人在想什么，网络在搞什么。但他实在太土了。&nbsp;&nbsp;<br />
<br />
　　你因为发言得罪过人吗？有没有因此遭遇压力或者被整？&nbsp;&nbsp;<br />
<br />
　　说真的，还从来真的没有过任何宣传部门直接找我说让我？改某些文章。有过三四篇文章被？除的，也都是网站和我商量，说这个能不能不写。我说随便，？就？呗。&nbsp;&nbsp;<br />
<br />
　　你觉得你的博客有可能被封吗？为什么？如果博客被封，你会怎么样？&nbsp;&nbsp;<br />
<br />
　　我会和我的博客合影留念。&nbsp;&nbsp;<br />
<br />
　　你觉得这个时代最缺少的品质是什么？&nbsp;&nbsp;<br />
<br />
　　中国的学生课本上所宣扬的，我们中国人假装拥有的所有美德，其实就是我们这个时代所缺少的。&nbsp;&nbsp;<br />
<br />
　　当你老到可以回首往事，给自己下一个结论的时候，你希望给自己一个什么评语？&nbsp;&nbsp;<br />
<br />
<br />
　　该生品学兼优、善良正直、独立进取、助人为乐、兴趣广泛、爱护同学，尤其是女同学。该生作为一个人类，是合格的。<br />
<br />
