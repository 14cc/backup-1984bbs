													<h2>与贵站“顾问”和菜头先生商榷个人博客删帖</h2>
						
						
																											<div id="postmessage_376130" class="t_msgfont">给和菜头先生的私人邮件：<br />
<br />
《和菜头先生刀下留人》 <br />
<br />
呵呵，标题党了。但作为主标题是可以的。副标题是：关于“我都快被吓哭了（http://www.hecaitou.net/?p=6559）”一帖，与和菜头先生商榷。 <br />
<br />
首先，感谢和先生勤写博客，不论是社会资讯的跟进评论，还是私人情怀的小品美文，在中文博客中都是上乘，每次点击都不会白跑。和先生还免费提供阅读，更是造福了中文网络的繁荣。 <br />
<br />
不过，和先生关于博客删帖一事的理论，我远远看了也实在难以同意。是我自己思虑不足，还是只是因为和先生过分性情所以省略了一些理论步骤呢？我想求教于和先生。 <br />
<br />
在我的印象中，中文网络上，论及删帖，最常见的理论是：私人产权主宰，和特殊权能赋予――我的地盘我做主。和先生所持的理由，好像也是如此。 <br />
<br />
但是，我认为，如果是作为一个自由理论的倡导者，仅仅从这里出发，恐怕支撑是不够的，至少，在自由伦理上是不通顺的。 <br />
<br />
实际上，一个人对自己的私人产权物有几乎绝对的主宰权，和一个人被赋予特殊权能，背后是自由理论的伦理根基：契约。 <br />
<br />
私人产权为何神圣？并非天生神圣，而是不得不神圣：为了人人自保，我们订立契约，禁止劫掠、偷盗等原始的占有形式。特殊权能何以正当？那也是因为有契约在先，某个人被赋予某种权能，即使这种权能必造成不公平和不对等，使用它也是有效和正当的。 <br />
<br />
而和先生在自己博客上删帖一事，我认为用私有产权主宰论来做全部概括是不够恰当的。和先生用了网络上惯用的博客留言区是客厅的庭院的比喻：留言者们是客人。但我想和先生应该会同意，这种比喻是有问题的：他们果真是您的客人吗？他们是您所邀约的（你们订立了口头契约，他们被允许进入您的“客厅”或“庭院”）？还是他们是您的私人朋友，而私人朋友，按照社会惯例（社会惯例是一些被完全默认了的契约的长期沉淀），他们可以不经您的口头邀约可能也可以进入您的“客厅”或“领地”？因为他们知道您不会随便就把他们“射杀”在地？ <br />
<br />
我想应该都不算是。您写博客然后开放了评论，如果非要用一个比喻，我认为，实际上，这相当于您租用了一个地方，然后搞一个开放式的沙龙。而按照文明社会的惯例，我们认为，这样的沙龙确实是可以自由提问和自由发言的，只要大家都遵守同样的文明社会的惯例。比如说，对于那些高声喧哗的，搅扰大家的，无关主题的，侮辱别人的，进行驱逐是毫无问题的。对应于博客就是，刷屏的，离题的，骂人的……等。对于这些留言，删除是毫无问题的。 <br />
<br />
然而，仅仅是不同意见的留言而非上面的情况，就进行删除，我认为这是说不过去的，是非惯例的，是有违某种默认契约的。 <br />
<br />
当然，和先生还有很充足的理由：他们的留言智力含量太低！而且唠叨！降低我博客的水准！ <br />
<br />
是的，在这每一次沙龙中，您是主导者，是主持人。规则完全是由您订立的。比如，同是沙龙，在技术上，您可以设定，这是一个单方面的主题沙龙，话筒只在我手里，这就是关闭评论。比如您尊称为先生的醉钢琴刘瑜就是这么干的。而她在这样做之前，做了声明。我认为是很有契约意识的。又或者，可以订立请帖制即准入制的规则，也就是私人聚会了。而这种规则，本身就是契约。再有，就如同您前面所说的：本博我的地盘我做主，留言智力太低要惨遭删除的，好让你们长点脑子。您这样说是无可厚非的，但是，我认为，最关键的地方在于： <br />
<br />
您没有进行过事先的特别声明和特别警告：我这里非同他处，是不按惯例行事的，即使我是自由主义者。您没有明白给出一个和留言者们遥相订立的契约。 <br />
<br />
您也许会说，在这件事之后，还有以前也发生过类似的事之后，我就早已经说过这样的话了，早已经做了宣示和警告。但是，我认为：1，在第一件事发生之时，您就已经是反惯例地行事了。2，您的发言是“淹没”在留言和众多博文中的，这样的契约方式不是非常有效，因为不可能每个人都要事先把所有前情都去了解一遍。更好的方式是，您把这样的警告或案例，做非常醒目的告示，比如作为简介或置顶，“回帖有风险，提交请谨慎”。否则，当人们抱怨您删帖时，实际上，他们的抱怨是有“理由”，而您的“理由”是不足的，特别是作为自由主义者。 <br />
<br />
所以，我认为，一个人自然是可以有自己的性情的，而且这非常可贵。但当这种性情是建立在契约之上，而因此不会那么“反常”时，这种“性情”才有可能是高贵的。而作为一个宣称喜爱自由价值的人，我认为这样做则几乎是必要的。 <br />
<br />
这里我想举关于我的几个例子（举这样的例子是暂时找不到其他例子），希望和先生理解： <br />
<br />
1，我以前在和先生博客上留过言，但当看到和先生说会删除留言，而在我看来删除标准并不明确后，我就没在和先生博客上留过言。包括现在，我也选择给和先生发信。且不论前面说的契约问题，我想和先生自己应该也知道，作为自由主义者，在讨论中，人格尊严的平等是如此重要，当您用“射杀”这类词语的时候，即使您是满带有童心的说法，但因为您确实执有手枪或火枪，它也不是那么有童心的。它展现的是一种内在的暴虐。而有尊严的人不会选择跑到您的枪口下。虽然我也知道，大部分时候，您也只是用拳头和刀与人对打，而不随便“射杀”。这也是我现在给您写信的原因。 <br />
<br />
2，我管理过一个讨论组，有人在进行“辩论”，而其中一个在过程中不断避让对方，并大篇幅地引用非原创也没有具体针对性的新闻来“嘲讽”对方时，我回帖说：以后类似跟帖请直接给处“标题-作者-提要-出处-链接”格式即可，否则将被删除，然后用这种格式重新编辑上帖。附带规则是“从此之后，不往溯”。我在某主帖作者可以有删除跟帖权限的讨论组里，因为不喜欢别人用太多的空格键，我也做出警告，同时“既往不溯”。我自知这肯定算性情怪异，但我认为只要有只要有契约在先，这至少就不是不正当的。 <br />
<br />
