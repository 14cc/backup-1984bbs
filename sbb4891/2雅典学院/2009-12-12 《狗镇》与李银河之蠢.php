													<h2>《狗镇》与李银河之蠢</h2>
						
						
																											<div id="postmessage_369891" class="t_msgfont">《狗镇》与李银河之蠢<br />
刘松萝<br />
<br />
看了李银河女士的《穷人不值得讴歌》以后，想过要写几句话。出于对她的尊重，没有写。至于尊重的原因，将在后面提到。现在写下来，是因为有人再一次贴出了这篇文章，并且再一次引发了仇穷者的欢呼。<br />
<br />
1.<br />
李银河的感想，来自影片《狗镇》。她是这样给我们介绍《狗镇》的：<br />
<br />
影片写一个黑社会老大的女儿格瑞丝（妮可&#8226;基德曼扮演）认为爸爸不正义，不想再做他的女儿，逃到偏僻深山中的狗镇。狗镇中的人们自私、麻木、残忍，把格瑞丝对穷人的浪漫想象打得粉碎。她的善良换来的是欺骗、冷酷、奴役、猥亵、强奸。她向往的天堂原来是地狱。最后她父亲来解救她时，她脖子上戴着枷锁，走路时用锁链拖着一个磨盘，因为躺在床上无法反抗，镇里所有的男人都强奸过她。她对父亲的权力从否定到肯定。当她父亲建议杀条狗吓吓镇上的人作为了断时，格瑞丝的建议是把全镇烧光杀光，并且冷血地亲手打死了欺骗她爱情的伪君子，没有一分钟的犹豫。<br />
<br />
李银河认为，影片“说白了就是一句话：你们都该死，你们不配活在这个世界上。”她还说，影片让“让每个看完电影的人在黑帮血洗狗镇的时候有一种强烈的快感，欢呼雀跃，觉得解气之极。”李银河进一步发挥道：“贫穷就是在残酷的生存竞争中败下阵来。失败有什么可表扬的呢？贫穷不是美的，是丑的；不是值得骄傲的，是值得羞愧的。或者是比较懒惰，或者是比较笨，或者是不够幸运。如果一个人活成了下层人，那就是活得最失败的人，被淘汰的人，有什么可表扬的呢？”<br />
<br />
这样引用，应该说没有断章取义了。对于本文的标题，网友立春先生不同意“蠢”字，认为有标题党之嫌。我犹豫过，最后还是觉得非“蠢”字不足以表达对《穷人不值得讴歌》的看法。<br />
<br />
2.<br />
贫富问题是一个永恒的主题。即使一个国家富裕了，只要把房价轻轻地炒上一炒，立刻就会增加一大批穷人。对于这个只有神才能够弄清楚的问题，我们切不可自以为是。茅于轼先生发表“为富人说话”的言论，除了加剧社会的分裂之外，没有别的的效果。过于积极地跟随茅老去发挥的人，事后多少有些尴尬。<br />
<br />
一个人从贫穷走向富裕，是一有些戏剧性的过程。迅速脱贫致富的人们，只要不是太夸张，太丑陋，做出一些怪异的举动也是不奇怪的。同时，公众有权要求知识界的人士坚持社会应有的准则。特别是，当他们步入富人行列的时候，更应该为社会做出表率。在《财富，那些被我们遗忘的常识》中，我还要指出：新富的人们，会情不自禁地说出“我们有钱人”，“我们名人”以及种种类似的话语。这很正常，同时效果很不好。不但社会效果不好，对个人的声望也没有益处。公众人物也这样说，影响就更坏了。<br />
<br />
我在以前说过，我们的社会是有严重问题的，人们其实并不快乐。那些准富人和想富的人，尤其不快乐。歧视穷人，就成为一种畸形的快乐。尽管会碰钉子，还是有人前扑后继，飞蛾扑火般地去做这种没有意思的事情。<br />
<br />
2003年，作家毕淑敏女士在《解放日报》上发表了随笔《坦然走过乞丐》。对此，何三畏先生写过《“坦然走过乞丐”的本事》。我在《文人投机的底线：不要接最后一棒》中认为，《坦然走过乞丐》是投机失败的例子，毕淑敏是接了最后的一棒。在另一篇文章里，我认为毕淑敏搭上了仇穷的末班车。<br />
<br />
现在看来，我高估了中国的知识分子。毕淑敏不但没有接最后的一棒，她简直就是领跑者了。2005年1月，人民大学的张惟英教授提出建立“北京人口准入制度”。2006年6月，钟南山院士建议恢复收容制度。他说：“偷窃与抢劫的人，和城市流浪人员只有一水之隔”。<br />
<br />
李银河之蠢，在于她是一位后知后觉者。穷穷富富的游戏已经做了无数次，但凡有点见识的人都不去参与了。李银河却在研究性学之余，把偶尔看到的东西当做发现，进而无限推演。<br />
<br />
从毕淑敏到张惟英，再到钟南山、茅于轼、李银河，一定要在贫富问题上过瘾，让人无法理解。还要指出的是，不懂得吸取教训，对马上就要出现的结果缺乏预见，通常是文盲的表现。<br />
<br />
3.<br />
李银河的博客文章，《随心所欲挥洒生命》，可以作为《穷人不值得讴歌》的注脚。她在谈到让生命更加精彩之前，先谈到了对老人的印象：<br />
<br />
我想，最让我害怕的还是像那些两眼发直、混吃等死的人一样，觉得他们的生活质量等于零，甚至是负数。就像那位一直花很多精力伺候两家老人的女同事所说：人到老了，就剩屎和尿了，什么也没有。拉屎撒尿就是他们全部的生活内容，最糟的是，就连这唯一的一件事还要人帮助，还要拖累别人。人活到这个份上，真是生不如死啊。其实我更怕的是将自己混同于身体还健康、神志还清醒、但却每天百无聊赖只能靠打打牌、遛遛弯了却残生的人。<br />
<br />
对于这番话，我不多评论了。算不算歧视呢？<br />
<br />
4.<br />
也许是觉得自己还不够蠢，李银河女士在《《狗镇》再思》中谈到了写作《穷人不值得讴歌》的过程：“文章确实是我写的。是我看了电影后很震惊还没醒过闷来的时候写的，发在博客上，但是很快就删掉了。因为政治上肯定是不正确的。”<br />
<br />
看了一场电影就可以做结论，“还没醒过闷来”就可以写文章，足见学术素养之差，或者有所退步。有人会说，这不是学术文章，只是博文。问题是，很多人认为《穷人不值得讴歌》很学术，还有人费了很大心思去织补其中的漏洞。因此，学者在面向公众讲话的时候需要谨慎。<br />
<br />
遗憾的是，泛娱乐化思维已经渗入到学术界。我以前说过，一部电影《色&#8226;戒》，就可以让学者改变对汉奸问题的看法，一部电视连续剧《闯关东》，就可以让人得出地主全都是勤劳致富，全都是很善良的结论。<br />
<br />
从学者到大众，思维方式和行为方式是不是太卡通了？</div>

							
							
							
							
