																											<div id="postmessage_369898" class="t_msgfont">5.<br />
讲一讲为什么要尊重李银河女士。<br />
<br />
在《《狗镇》再思》中，李银河告诉我们：“自从我写了《狗镇》的观后感，所有的朋友都跟我急了。李春光急赤白脸地问这是不是真是我写的，准备去辟谣；林春来信说，我相信你写这东西的时候不是你自己了。”<br />
<br />
为什么林春女士要那么说？原来，1978年，林春、李银河在《中国青年》杂志上发表了《要大大发扬民主和加强法制》。那时，李银河年仅26岁。<br />
<br />
文章的影响是很大的。这是自文革结束以来，第一篇公开主张民主的文章。我看到不少人在回忆往事的时候，都提到了文章的影响。对我来说，文章证实了我以往的想法是正确的，让我得到了鼓舞。从此以后，我一直没有停止写作。<br />
<br />
因此，我一直把李银河女士视为民主的启蒙者。即使有不同的看法，也很少去说什么。<br />
<br />
以后，李银河女士去研究社会学，并且偏重于性学，是有贡献的。同时，面对公众，李银河对性学的宣讲有时有些激烈，有内衣外穿之嫌。<br />
<br />
6.<br />
再谈谈林春的那句话，“我相信你写这东西的时候不是你自己了”。一般来说，这么讲话的语气是很重的。虽说人不是为别人活着，包括不是为朋友活着，听不懂朋友的忠告还是令人遗憾的。<br />
<br />
我想，曾经被人看做先驱者的人，应该让自己保持完整。我说的完整不是继续做战士，而是不要过于违背自己的初衷。有人会说，这样会很累，也很假。我要说的是，一个人得到了虚名，就要为此付出一些代价。当然，这代价不是无限的。<br />
<br />
我认为，李银河女士关于贫富的那一番话不但不正确，而且缺乏智慧，不够体面，有损于自己的声望，也有损于王小波先生的声望。<br />
<br />
我注意到，在《《狗镇》再思》中，李银河是有一些进一步的思考的。<br />
<br />
7.<br />
讲一点轻松的事情。<br />
<br />
李银河在文章中用了一些北京方言词汇，比如“血里糊拉”、“醒过闷来”、“急赤白脸”。作为同乡，读起来是很生动的。<br />
<br />
不过，讨论只要问题的时候，使用方言会导致思维的扭曲。<br />
<br />
8.<br />
有这样一个现象：当一个人正面的社会效用在递减的时候，他的社会影响却还存在着，甚至还有可能递增。<br />
<br />
作为名人，应该谨言慎行，以对得起自己的拥护者，配得上自己在社会上的声望。必要的时候，应该隐退。<br />
<br />
对于长期名不副实的人，社会应该有淘汰的机制。这样做，对名人是残酷的。不这样做，对公众是残酷的。<br />
<br />
对于李银河女士，我认为，作为社会学家，起码要讲逻辑，讲实证。<br />
<br />
我看到，李银河女士谈到过问题研究完了应该怎么办。对此，以后还要讨论。<br />
<br />
9.<br />
最后，我想谈一谈我在《警惕经济极端主义》中要涉及的问题。<br />
<br />
有人会说，茅于轼和李银河的言论，受到不少人的支持。我想，其中的原因不外是两个：第一，他们的观点是正确的。第二，他们的观点是蛊惑性的，迎合了极端主义思潮。目前，我倾向于后者。<br />
<br />
现在，我们面临的社会是割裂的，气氛是紧张的。极端性的言论，不论其指向如何，都会引起一部分人的拥护。作为知识分子，我们必须要有责任感，不要加剧社会的紧张和分裂。特别是，不可以陶醉于欢呼之中，不断地加大自己的声音。<br />
<br />
我认为，有些专家学者的主张已经接近社会达尔文主义，接近极端主义。说得严重一些，是另一种类型的法西斯主义。强化这样的主张，并且把这样的主张与自由主义和市场经济捆绑在一起，只能为极左势力提供口实。<br />
<br />
按照历史经验，左的和右的极端主义是相通的。不仅在思维方式上相通，而且其拥护者也是可以迅速转换的。就是说，公众会从极左转向极右，或者从极右转向极左。<br />
<br />
在一个割裂的社会，我们切不可以让对立和对抗升级。你鼓吹掠夺穷人，淘汰穷人，对方又何尝不是这样想呢？斗来斗去，不过是阶级斗争而已。<br />
<br />
具体来说，李银河女士博客中的杀杀杀，以及有些学者的弱肉强食不过是意淫而已。一介书生，真的进入丛林，谁先被淘汰呢？<br />
<br />
