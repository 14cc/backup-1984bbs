<br />
几天前的一个中午，下课后班上的一群男生一起到校外的小饭馆吃饭。这群人，来自天南地北，不同的民族。<br />
<br />
其中一个同学的外号叫“田鸡”，于是有同学在点菜时开玩笑说，炒田鸡来吃吧。很快，这个说话的同学就和身旁的藏族同学莫名其妙的打起来了。当然，大家很快就将他们分开。<br />
<br />
等大家再次坐定之后，我们才得知，原来那个藏族同学他们那儿不吃田鸡，那是他们的宗教信仰。<br />
<br />
在旁人看来，这完全就是一个误会。<br />
<br />
是的，这的确是个误会。然而这样的误会其实是可以避免的。<br />
<br />
中央一直都在说要团结各族人民，尊重各民族的宗教信仰，然而在我所接触到的所有学校教育中，没有任何关于我国各民族宗教信仰等民族文化的教育课程。<br />
<br />
对于中华民族有五十六个民族，我们早就耳熟能详了，歌曲经常唱教材经常说。到大学之后上了公选课之后才知道其实中国的民族数目岂止五十六呢，至少在一百以上，只是为了便于统计，加上很多民族不断被汉化，所以官方规定我们这个国度只有五十六个民族。而我所知道的，也仅止于此。<br />
<br />
虽然很多民族已经被汉化，但是对于那些数目庞大的未被汉化的民族，既然我们口口声声说要团结各民族，就应该让我们了解各民族的文化。至少，我们应该开设课程，让大家知道各民族的一些基本的禁忌，不至于在相处时发生冲突。<br />
<br />
国家收了纳税人的钱搞教育，就应该为国民教育作出应有的成绩。<br />
<br />
在宣称要团结各族人民的口号之下，丝毫不让大家彼此了解对方的宗教信仰，这不是一个政府应该有的做法。尽管今天的汉族人绝大多数没什么信仰，但对于西藏和新疆这些地方的人民，他们多数是有信仰的，我们要和他们和睦相处，就必须从了解他们的民族，了解他们的信仰，尊重他们的文化开始。<br />
<br />
国家教育不做有利于人民的事情，不搞通识教育，是不是应该承认自己对不起人民对不起国家呢？而政府在这件事上是不是应该负责呢？<br />
<br />
那些专门拿老百姓的钱不为国家的教育做事的教育部门，是不是应该为自己的行为感到羞耻呢？<br />
<br />
