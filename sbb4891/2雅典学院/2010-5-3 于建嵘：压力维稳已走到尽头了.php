													<h2>于建嵘：压力维稳已走到尽头了</h2>
						
						
																											<div id="postmessage_594772" class="t_msgfont">在以往的研究中，我曾用刚性稳定来表述目前中国的稳定状况，压力维稳是这种刚性稳定的维持机制。因此，厘清压力维稳的主要特征、原因和后果，有助于我们进一步认清刚性稳定的本质。<br />
<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 一、压力维稳的主要特征<br />
<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; (一)重视事后处理胜过源头预防。<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 尽管中央文件再三强调从源头上预防和解决问题，但在实践中却相反。一些群体性事件之所以发生，其中非常重要的原因就是没有做好源头的预防和治理工作。以备受关注的贵州瓮安事件为例，2008年6月28日下午，当地部分群众因对一名女中学生的死因鉴定结果不满，聚集到县政府和县公安局。之后，一些人煽动“不明真相的群众”冲击县公安局、县政府和县委大楼，“少数不法分子”趁机打砸烧，致使多间办公室和一些车辆被损坏。事件发生后人们从各方面找寻缘由的时候才发现，原来这次影响巨大的群体性事件在发生前已有很多征兆。据《半月谈》杂志披露，瓮安是一个矿产资源丰富的地方。近年来，经济社会得到较快发展，但在移民搬迁后期扶持、违章建筑拆除、矿权纠纷处理、国企改制中，出现了各种矛盾，沉积的有影响的重点信访案件就有20多起，各种矛盾纠纷没有得到及时化解。贵州省委书记石宗源在通报事件解决情况时也指出，瓮安事件表面的、直接的导火索是女中学生的死因争议，但背后深层次原因是当地在矿产资源开发、移民安置、建筑拆迁等工作中，侵犯群众利益的事情屡有发生。换句话说，瓮安当局的不当执政行为，积累了大量民愤，由于没有从源头上及时有效地疏导和解决，最终酿成了这次大规模的群体性事件。<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; (二)重视基层，注重“实战”。<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 目前，我国实行的中央、省、市、县、乡五级管理体制已经越来越不适应市场经济的发展和社会管治的需要。尤其是随着改革的深入，各种深层次问题逐渐暴露，各种矛盾凸显。在这种情况下，提高基层政府的社会管控能力就显得非常必要。俗话说，基层不牢，地动山摇。不过，由于层级过多，很多政策从中央到地方要经过层层过滤，致使很多政策到基层已经严重扭曲变形，失去了本来的治理效力。这就是人民群众常说的“中央政策是好的，到下面就走样了，是歪嘴和尚念错了经。”因此，为减少层级过多带来的信息流失，中央采取各种措施加强与基层的直接联系。比如让县委书记、基层公安局长进京轮训。邀请相关专家学者给这些基层官员讲解各种维稳知识，包括如何应对群体性事件等。尤其值得一提的是，在培训过程中开始减少理论知识的学习，逐步加强技术性、实用性处理方法的探讨和应用。<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; (三)政绩考核功能异化。<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 在中国，上级对下级的政绩考核是决定官员任免和升迁的重要指标。政绩考核的项目繁多，经济、政治、社会各方面都会涉及到。尤其是对一些重要指标实行“一票否决”，使得基层官员不堪负重。为了完成重大任务，地方官员往往会采取“人民战争”、“群众动员”的方式。这就造成了真正的工作没有时间和精力去做，为了应付“一票否决”的事项整天忙得焦头烂额。以信访责任追究为例，中央一直强调把矛盾化解在基层，以减少北京和各省市面临的工作和社会稳定压力，逐级下达信访工作责任目标考核。面对中央的压力，地方政府往往并不是去解决问题，而是对上访群众采取的截访、销号、拘留、罚款、劳教、判刑、连坐等控制手段压制上访人员。当然，我们不是说地方政府根本不想解决问题，因为有很多问题确实不是地方政府能够解决的，也有很多问题是任何政府都不能解决的。实际上，有些时候基层官员也处于进退维谷，左右为难的境地：对上要承受上级政府的压力，如果硬性规定的“一票否决”考核事项出了问题，别的工作做得再好也没用；对下要应对民众的各种诉求。<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; (四)成本高，投入大。<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; “维稳工作”造就新的利益部门。很多时候地方政府迫于维稳的压力，不惜花费巨额经济成本。以当前中国信访实践中存在的拦截上访民众的截访为例，其所花费的经济成本已成为了地方政府的沉重负担。例如，十七大期间，为了保证所谓“零进京”，从9月28日到10月25日，河北省某个上访问题并不严重的县组建了驻京工作组进行截访和劝返，人员包括主管信访工作的县委副书记、县政法委书记、信访局、法院、检察院、公安局等部门的主要领导和工作人员 25人。这个县4个有上访人员的乡镇，分别还派出2至3人到北京值班，全县到京接访的人近36人。据最保守的估算，这个期间，整个河北省进京接访人数达到了5000多人。全国像河北省这样进京上访较多的省近10个，加上其他省市，全国在北京接访人员近10万人。这么多官员和工作人员到北京来接访，对基层政府来说是一笔不小的行政开支。对此，山东一位基层领导曾坦言：“上面传信来要我们去接人，立即派出两三个人过去，连吃带住加汽油费，一次就花了一两万元。”截一个上访者的费用有时足以解决其实际问题。<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; (五)基于短期利益考量。<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 在中国的政治生态中，稳定是压倒一切的，其他任何事情都要以稳定为基本前提，任何工作都要给维稳让路。为了维稳甚至可以不惜给正常经济发展、民众日常生活带来不便和负面影响。同时，在“维稳压倒一切”的观念主导下，政府采取的社会管治方式总是简单化和绝对化。目前中国的“群体性事件”，实际上有许多是工人和农民、市民等群体为了自身的利益而集体上访等行为。这些事件并不是针对政府的，政府至多是调解者或中立方。但由于某些地方政府把这些行为定性为影响社会稳定的事件，而为了维护社会稳定，各级政府特别是基层政府，就不得不采取各种维稳行动。因此，这种压力维稳是基于现实考虑远大于中长期利益的考量，有头疼医头，脚疼医脚之嫌。<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 二、压力维稳的主要原因<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; (一)现实的需要。<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 对社会稳定存在威胁和现实挑战的主要有这样一些方面：其一，恐怖活动的挑战，每次重大国际活动的安保工作都会成为维稳重点之一，如奥运会、世博会、亚运会等。其二，民族地区的“骚动”。如新疆、西藏等地区民族分裂分子制造的骚乱。其三，群体性事件，以征地、暴力拆迁引发的为代表。其四，信访活动，特别是进京访和群体访。其五，犯罪率攀升、治安问题日益严重。特别是暴力犯罪、黑恶势力犯罪、街头犯罪，以及社会影响大经济案件，如非法集资等。其六，邪教。以上这些分类并不严格，有交叉。<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 而在面对因为群体性事件引发的社会不稳定时，因为我们的领导干部任用机制等是几年一换，使其更希望在自己任内压制矛盾维持现状，而不是从根本上解决矛盾。这一点在基层表现的尤为显著，很多基层官员缺乏责任感，也没有化解矛盾的勇气和动力，对一些问题能推就推，能捂就捂，只要矛盾不激化，不影响自己的政绩，他们是绝对不会主动解决的。因此，很多官员不能用“平常心”来看待社会冲突，更不能认识到社会冲突具有缓解社会政治压力的减压阀作用，而是将稳定视为一切，为了维稳经常不惜代价。<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; (二)体制性原因。<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 当前中国的政治结构在制度层面上是高度中央集权的，然而，在现实政治操作层面上，往往是各级地方政府通过对中央政策的修正甚至反动来谋求其相对独立的利益。例如，在一个县的政治舞台上，真正的主角是县委书记。在制度设计上，县委书记只是中央的一个代理人；在政治现实中，他却是真正的权力实施者。在制度设计上，县政领导的权力一方面来自中央政府的授权，另一方面来自县域民众通过人民代表大会的授权，因此他们的权力应该受到上下两个方面的监督和制约。然而在实际运行中，县政领导却可以有效摆脱两个方面的监督，在县的施政方面行使没有任何有效监督和制衡的权力。<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 三、压力维稳的影响和后果<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; (一)一段时间能得到部分民众的支持。<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 运用高压手段维护社会稳定，在短期内可能有效果，也能迎合那些出于种种原因支持“维持现状”人的需要。但是，这种压力维稳存在着巨大的风险，一旦出现问题就会不惜代价强行解决，难免会侵犯民众的合法权益，导致民众的心理认同感幻灭，支持率降低。因此，从长远来看，压力维稳不但不能保障稳定的长期延续性，也不能真正实现民众渴求稳定的愿望。<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; (二)基层官员不堪重荷。<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 中央和上级政府把维稳作为一项重要的政绩考核指标，试图约束基层官员的不当执政行为，但是这种约束不但没有发挥作用，反而异化为维稳压倒一切的现实考量。致使很多基层官员不得不疲于奔命于和维稳相关的事项，尤其是在国内外有重大活动的所谓“敏感时期”，基层官员更是得紧绷早已脆弱的敏感神经，广泛动员群众，把维稳作为头等重要的大事来抓。近日，一个在河北基层乡镇担任领导的人给我的一条短信道出了他们不堪重压的窘境。他说：“我们信访维稳压力非常大。只要有进京赴省上访就免职，您说好不容易争取到一官半职，容易吗？我现在年纪还不大，但从奥运安保以来，血压就高了，非常上火。信访是目前的大气候，尤其是个别闹访户，越是敏感期越是闹。您是专家，这种形势还有可能好转吗？基层干部都快被压死了。唉！”可见，当前的很多基层官员已经不堪重荷，面临着“对上扛不住，对下管不住”的尴尬局面。<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; (三)财政支出越来越大，影响经济社会发展。<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 压力维稳需要较多的财政支出和资金投入，尽管目前还没有权威的全国维稳成本统计，但仅就部分地区的情况来看，维稳支出每年以两位数增长，维稳经费相当于甚至超过政府的民生支出，似已成为普遍现象。据公开的信息，广州市2007年社会维稳支出44亿元，比社会保障就业资金35.2亿元还要多出许多。并且很多时候一些维稳经费是完全可以避免的额外支出，比如前面提到的为了拦截一个上访群众需要花费上万元，这笔钱如果用于解决上访群众的问题可能会绰绰有余，这样不但没有解决问题反而把资源白白浪费掉了。这不但增加了地方政府的财政支出，而且使地方官员把主要的时间和精力都放在维稳工作上，疲于应付，这就背离了政府为了公共利益、为增加人民福祉而建立的初衷。不过，增加的财政支出并不由官员个人承担，而是由纳税人来买单。所以对基层官员来说，增加维稳支出虽属无奈，但也会尽力而为之。从长远来看，这种额外的维稳支出会对当地经济社会发展产生负面影响，而且压力维稳经常掩盖问题，难以从源头上真正解决矛盾。<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; (四)矛盾不解决总在那里，压力越大反弹越大。<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 在当前中国的政治系统中，压力是循着自上而下的路径传递的，但是底层民众的问题如果长期得不到解决，他们就会突破这个传递路径，直接向最高执政者诉求，这样就会打破官僚制的层级，扭曲压力的传导机制，最终形成中央压地方，地方压民众，民众压中央的恶性循环。纵观中国几千年治乱兴衰的历史，没有哪一个统治者能够长期掩盖和回避矛盾。<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 四、解决目前压力维稳的主要建议<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; (一)变刚性稳定为韧性稳定。<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 为了解决目前的压力维稳机制，需要适时推进一些重要的制度变革和制度建设。在我看来，首先需要进行一系列的社会改革，以使“刚性稳定” 转变成“韧性稳定”，其中最重要的就是建立公平公正的社会分配体制，让社会各阶层真正共享经济发展的成果。改变目前的行政压力体制，建立县级政治分权，并通过司法改革，树立国家的法制权威。目标是使中国县政建立在行政分权和政治分权基础上的民主自治，以此来解决威权政治的缺陷，使政治体制增加韧性。与此同时，国家还应该充分满足民众的参政意愿，切实保障公民的有序政治参与，这也是每个公民享有的基本政治权利。改进和完善政治制度，真正落实宪政体制下的公民权利，把公民的利益的表达纳入体制内，是实现国家长治久安的可靠保障。<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; (二)变静态稳定为动态稳定。<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 对于“动态稳定”与“静态稳定”的差别，政治学者俞可平解释说，“静态稳定”就是禁止做什么，比如老百姓有什么不满，禁止他表达，也就是以堵为主；而“动态稳定”就是有不满说出来，如果有道理就赞成他，并进行制度调整，即以疏为主。目前中国的社会稳定状况是典型的以压和堵为主的静态稳定，民众的权利意识正在逐渐觉醒，但缺乏真正代表他们的利益表达机制，民众的谈判能力弱小，还经常受到权力、资本拥有者等强势群体的侵害，在这种情况下，民众的不公正感和被剥夺感可能成为诱发社会不稳定的因素。因此，要解决目前的压力维稳机制，必须建立一套完善的利益诉求机制，要建立一个社会安全阀机制，让民众表达自己的利益诉求，让社会情绪得到宣泄，否则就会形成“蝴蝶效应”。<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; (三)变“维稳”为“创稳”。<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 解决目前的压力维稳机制，需要树立科学的维稳思维，变被动“维稳”为主动“创稳”。这首先要突破简单理解“稳定压倒一切”的思维定式，改变那些不计成本和后果，一切都要为稳定让路的做法。走出这种思维定势需要新思维，可以具体为三句话：面对目前的群体性事件，社会需要理智，执政者需要智慧，研究者需要品格。政府官员在对待涉及群众切实利益的重大问题时，一定要公平公正、及时合理地解决，坚决杜绝敷衍塞责、滥用职权等不当行政行为，从源头上减少矛盾纠纷和不稳定因素，达到主动“创稳”的效果。<br />
<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 总之，有矛盾和冲突并不可怕，关键是怎样对待这些矛盾和冲突。社会学家们认为，社会冲突本身也是存在正功能的。一个社会不可能没有矛盾和冲突，关键是要把它们控制在一定的范围之内。也就是说，一个国家最可怕的事情不在于发生什么问题，譬如群体性事件等等之类的具体问题，而是出现了社会冲突事件后，我们该怎么认识它、怎么管理它。正常社会有自动纠偏、自己解决社会矛盾的能力。与其像现在这样自上而下压力维稳，制造“高压锅”，不如将精力放在建设正常社会上。</div>

							
							
							
							
