													<h2>中国人到底是凶悍还是懦弱，道德是讲多了还是少了？</h2>
						
						
																											<div id="postmessage_600116" class="t_msgfont">　　中国人的毛病到底是什么？人民是凶悍还是懦弱，道德是讲多了还是讲少了？这是两个决定性的问题，因为它的答案一旦错了，结论就是相反的，效果也是相反的。<br />
　　我认为：中国人的毛病在于不够凶悍，且道德讲得太多。<br />
<br />
　　先说道德吧。<br />
　　道德当然有用，但是嚷嚷道德（即鼓吹道德）是没用的，这是最基本的道理。没有制度性的保障，嚷嚷得越多，反作用就越大。没有哪个国家的道德建设是靠鼓吹而实现的。<br />
　　但是，这里面有个微妙之处，那就是：嚷嚷道德虽然不好，但是嚷嚷缺德（即揭露人们的缺德之处）却是正当的；因为嚷嚷缺德是正当的，很多人会很自然地过渡到嚷嚷道德上去，也就是说，过渡到有害无利的道德说教上去。<br />
　　指出人们的缺德，是揭露社会现实，它应该过渡到找出社会现实背后的实质性原因上去，而不能简单地归结为道德问题。因为制度法律是社会的基础，道德现象是建立在这个基础之上的。<br />
　　当然，也不能说发现了缺德现象，就一味指责社会制度，道德和传统也有关系；当然，这个关系不是决定性的，但终究不能说没有，下面就会提到。<br />
<br />
　　再说凶悍。<br />
　　中国人自古懦弱，古代西方、日本的支柱阶层是骑士、武士，而中国的支柱阶层是儒士；西方的贵族一般都是军事贵族，中国不仅军事贵族比例较少，武官还要礼敬文官；不仅汉人王朝如此，满清也是如此。<br />
　　这种情况，在古代当然有其道理，世界上的大一统王朝，或多或少都会有重文轻武的倾向，这是很自然的结果，也可以在一定程度上防止打内战。但是，有道理不等于说这种结果就一直有利。到了太平天国战争和同治回乱时期，大部分地区武备废弛、民风柔弱，以至于一小撮教匪（其信仰都他妈是从摩西那里衍生出来的）就可以随意屠杀人数众多的和平居民。此后，到了抗日战争时期，这种束手就杀的悲剧又在较小程度上重演。然后，就是大量人民束手就毙的大饥荒。<br />
　　直到现在，几个维吾尔毛贼都可以在大庭广众之下吓住人数众多的汉人。<br />
　　即便如此，道德家们还在指责中国人民凶悍！他们要把中国人民阉割到什么程度？<br />
　　好在，实际上也没太多人拿道德家们当回事了，从理论上分析一下他们的邪恶吧：<br />
　　大多数人的懦弱往往使少数暴徒得逞，这是历史上屡见不鲜的悲剧；同样糟糕的是，道德家们对“适当的暴力”、“不当但有情可原的暴力”以及“绝对不当的暴力”不加区分地进行谴责，导致个别人一旦铤而走险，就有完全不考虑道义的倾向：反正我是坏蛋了，当到底好了。<br />
<br />
　　怎么办？<br />
　　有个说法叫“脱亚入欧”，或者加上“彻底”二字：彻底脱亚入欧。<br />
　　这个话有道理，但严重不准确。要知道，坏东西总是容易学的，脱亚入欧的概念太宽泛，其结果学的肯定是那些坏东西。尤其是那个教人当奴隶的宗教：基督教的思想，就很容易顺着中国人的奴隶性格爬进来；左翼思想也是发源于西方的，针砭时弊，很容易学。这样的脱亚入欧，还不如不入。<br />
　　西方的精髓在希腊-罗马的异教时期，民主、宽容（多神教比一神教宽容多了）和英雄主义精神都发源于希腊，文艺复兴和打破基督教教会统治则被公认为西方近代化的开端。<br />
　　因此，我们应该做的，不是“脱亚入欧”而是“希腊化”，或者说“补上希腊化这一课”。中国在古代错过了希腊化进程，现在要补上了。<br />
<br />
<br />
*：不能因为日本讲“脱亚入欧”而获得成功，我们就盲目认可这个词汇。明治维新的时候，西方的左翼势力还未成形，而且西方人是以武力的面目出现在日本人面前的，不会让日本人有什么误解的余地。</div>

							
							
							
							
