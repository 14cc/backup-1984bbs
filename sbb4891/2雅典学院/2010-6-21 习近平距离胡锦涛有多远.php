<br />
第三，中国在胡锦涛的领导下，10年里成绩不少，但是问题也很严峻，习近平属于守业类型，而中国如逆水行舟，不进则退，守业类型不适合中国的实际情况。具备一定西方思维的薄熙来更有能力在开创中保持前进，给中国的发展注入新的活力。<br />
