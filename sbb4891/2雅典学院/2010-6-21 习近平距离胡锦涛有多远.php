																											<div id="postmessage_695451" class="t_msgfont">在中国领导人胡锦涛、温家宝离任之后，中国政坛三位引人注目的新星为习近平、李克强和薄熙来。据说江泽民舍弃薄熙来力挺习近平的主要原因是习近平性情比较温和，在江泽民百年之后，他儿子江绵恒的那些个破烂事他再也罩不住，而习近平的温和的个性应该会放他儿子一马。<br />
<br />
习近平因此被看作是胡锦涛的接班人。作为中国党政军最高领导人，需要具备多重优秀素质，而习近平似乎离这个位置还有一段距离。<br />
<br />
远的不说，就说习近平日前访问新西兰，遭遇抗议一事，习近平的处理方式与胡锦涛相比就差了那么一大截。胡锦涛访问新西兰时，那些FL功台独藏独人士也举旗抗议，而胡锦涛下车后居然面带微笑，向抗议人群挥了挥手，然后平静地接着该干啥干啥。有人说胡锦涛也许不知道那是示威抗议人群，我坚决不同意这样的看法，胡锦涛是从西藏父母官走向中央的，他怎么可能看不出那是藏旗？胡锦涛的气度，让人钦佩。作为一个中国领导人，能做到这样实在不容易。吴邦国访问新西兰时，绿党议员也举藏旗抗议，吴邦国的反应是，不走铺有红地毯的正门，改从侧门进入，吴邦国到达时，两名中国保安组成人墙，挡在绿党议员前面。此举虽然不太大气，但是也还说得过去。而习近平此次抵达惠灵顿国会大厦，保安人员先与绿党党魁发生肢体冲撞，可能中方保安用雨伞撞击对方时还划破了对方身体的某一部位，因为据主流媒体报道，绿党党魁出血了，然后中方保安又把他的旗子抢过去踩在脚下。舆论一片哗然，绿党党魁还向惠灵顿警方报案了。<br />
<br />
唉，我真不知说啥好。那个绿党党魁在习近平到达之前就在国会大厦门口对主流媒体宣称他就是想看看中方这次如何反应，并且得意洋洋地说上次绿党的抗议让吴邦国连正门都不敢走了，明显挖个坑等着中方代表团跳进去，中国方面反应越激烈，越违反西方民主国家的法规，他就越有借口抨击中国。<br />
<br />
这么明显的一个坑，习近平还真就跳进去了。这下子新西兰人不乐意了，说怎么中国人到我们的国家对我们的议员动手啊。其实这件事情有许多解决办法。咱就说咱这苯脑子里想得出的办法吧。<br />
<br />
办法一，抢他的旗子，没问题，不就是要他没有旗子出现在媒体面前吗？这有何难？抢就是了，但是谁去抢？绝对不能是中国的保安和任何中国人去强。这事要换作我去干，非常简单，手里揣把锋利的小刀，习近平来时咱上去给他那旗子随便划一道，他立刻傻眼。我是新西兰公民，这事要闹起来，主流媒体也只能说是新西兰华裔对绿党党魁不满，跟中国领导人扯不上关系。按照新西兰法律，我给他旗子划破了，啥事没有，最多陪点钱，他那破旗帜跟纱巾一样小，值几个钱？就算我那天没空，找个新西兰华人来干这事有啥难度？不是有那么多华人华侨华人社团高唱爱国口号吗？那就让他们来点真格的。<br />
<br />
办法二：中国使馆人员或者保安准备一面五星红旗和新西兰国旗，一面旗子上有两个国旗，对吧，象征友好，然后习近平一到，两保安把大旗一拉，正好挡住那绿党党魁，他还没反应过来，习近平已经进去了。<br />
<br />
办法三：习近平走过去跟他握手，他要跟习近平握手，就没法扯旗子，因为他是两手拿着那面旗的，习近平握手时跟他说，希望他把抗议写成书面材料，正是递交使馆。如果习近平这样做，西方媒体会认为中国领导人已经仁至义尽了，他如果还要去扯旗子，别人会认为这人无理取闹。我一直认为，中国领导人应该灵活一些，至少在外界面前要做成尊重民意的样子，不管是抗议的，FL功的，都可以让他们写成书面材料去使馆递交，一定要给别人一个说话的渠道。至于递交后使馆可以把材料扔垃圾箱里，对吧，谁也不知道。西方人就是喜欢做表面文章，至少在表面上得是公正的，习近平的保安公然袭击新西兰国会议员，犯了西方的大忌。<br />
<br />
再说说其他两个领导人。据说胡锦涛特别看好李克强，我分析原因有二，李克强也是安徽人，而且跟胡锦涛一样，没有任何背景，完全凭借自己在政坛打拼。我个人也认为没有背景的人能力智力都要强一些。<br />
<br />
如果我有投票权，中国未来的领导人我会投薄熙来一票。为什么呢？首先，我们看胡锦涛的夫人，是胡锦涛清华的同学，典型的智慧知识型女性，胡锦涛选择这样的人做伴侣，说明他自己就得具备欣赏知识智慧的能力。薄熙来的夫人是一名律师，脑瓜也够用。而习近平择偶似乎是重色，这样的人作为国家领导人，不如薄熙来。<br />
<br />
