													<h2>Google退出中国市场 并非出于政治道德</h2>
						
						
																											<div id="postmessage_432429" class="t_msgfont">作者：倍可亲网站爱华/2010年1月12日<br />
<br />
美国Google公司的高级副总裁、首席法律顾问David Drummond在Google官方博客上以高姿态发表了一篇题为《A new approach to China》的高调文章。文中威胁道，“公司已决定不愿再审查我们在Google．cn上的搜索结果……我们承认，这很可能意味着公司将不得不关闭Google.cn，以及我们在中国的办公室。”。首先我在这里需要指出一个问题：有很多文章说Google关掉Google在中国的经营就意味着中国网民不能进入Google搜索。这些文章如果不是因为作者无知就是因为作者言为别有用心而故意误导读者。这点在本文下面将论及。<br />
<br />
　　Google的这一高调声明立即在西方引起了轩然大波。这完全不会出乎大多数人的意料。但Google真是因为中国的网络监察制度而发出这一威胁的吗？我看完全不是。<br />
<br />
　　Google曾对中国市场寄予极大希望<br />
<br />
　　根据华尔街日报报道，中国市场是世界最重要市场之一，几乎被所有大企业所重视。在网络方面更是如此。截至去年6月，中国拥有3.38亿互联网用户，超过任何其他国家，并且还处于快速发展期，具有广大的市场潜力。这点Google不能等闲视之。美籍华人李开复曾位居微软全球副总裁。但Google为了开拓中国市场，2005年不惜重金将李开复挖过来。并且与微软撕破脸皮，打了一场声势浩大的官司才赢得李开复。后来又高薪聘来周韶宁。这些都反映出Google对于一个急速发展的市场的迫切性和强烈的市场期待。因为他们以为李开复、周韶宁更懂中国文化，更懂得如何让Google转化为一个本土化的搜索公司，能够更好的取长补短，与百度等本土搜索厂商一决高下。<br />
<br />
　　李开复到中国主管Google在中国业务后，Google很快就动用其资源就大张旗鼓、声势浩大地推出了针对专门针对中国市场的Google.cn网站。这样一来，中国人就能同时使用Google.cn和Google.com这两个不同的搜索网站了。目前Google在中国拥有雇员约800人。<br />
<br />
　　Google在中国遭受市场无情淘汰<br />
<br />
　　在四年前的对外经贸大学举行的“2006商业灵感论坛”上，当时的互联网搜索新秀百度上选“2005战略管理标杆企业”，零点研究集团董事长袁岳挑起话题：“5年以后 Google和百度是什么样的关系？那个时候Google 比百度牛还是百度比Google牛？？。对此问题百度公司创始人、董事长兼首席执行官李彦宏就满怀信心地预言：“5年以后（即2011年），Google和百度之间的关系是百度一枝独秀，从市场份额来说，大家很难 看到Google了。”<br />
<br />
　　百度的财务总监王湛生出席了当天的论坛，他证实了这一话题。他说：“随着中国语言不断发展，中国独特规模经济体不断成长，我们认为依赖进口技术做中国的事情难以持久，我们相信中国人会用自己的搜索引擎技术了解中国文化。”<br />
<br />
　　在2006年，百度以56.6%的市场占有率稳坐第一，而Google只能以32.8%屈居第二。但更让Google大受打击的是，Google的这32.8%的市场份额中的99%是由Google.com贡献的。对此不尽人意的状况Google创始人Sergey Brin 就指出：虽然Google.com和Google.cn在中国同时并存，但只有1%的中国用户使用后者，这主要因为Google.cn的搜索结果在质量和数量上都无法同主搜索引擎相比。但令人啼笑皆非的是Google在中国本土市场上主推的却是Google.cn而非Google.com。<br />
<br />
　　根据中国CNNIC的在2009年9月公布的统计数据，仅有可怜巴巴的12.7%的中国网民将Google作为搜索网站的第一选择，比2008年大幅下降3.9%。而中国本土的百度被高达77.2%网民作为第一选择。<br />
<br />
　　虽然经历多年的市场运作，但无情的市场竞争的结果证明谷歌在中国是失败的。虽然在中国投入了大量的人力和物力，Google在中国的市场处境并没有任何改善。2009年9月，谷歌遭遇又一次重大挫折，它于2005年从微软(Microsoft Corp.)挖来负责中国业务的李开复离职，创办自己的中国互联网投资企业。<br />
<br />
　　西方网络公司在中国的惨败是普遍现象<br />
<br />
　　事实上Google在中国的惨败并不是特例，而是中国网络内容服务市场上的普遍现象。<br />
<br />
　　在电子商务市场淘宝击败Ebay。淘宝和Ebay的易趣貌似孪生兄弟，实际上根本不同，这一差别使得战斗一开始就决定了胜负，只不过人们花了3年时间才明白其中的道理。 <br />
<br />
　　1999年，从哈佛回来的邵亦波带着40万美元风险投资来到上海，创办了当时国内第一家电子商务拍卖网站。当然，这个商业模式不是他凭空想出来的，不论从网站结构、赢利模式还是收入结构上，从易趣身上都能找到美国eBay的影子，甚至邵亦波和eBay的CEO惠特曼同出哈佛门下。到2002年的时候，eBay投资3000万美元现金，获得易趣33%的股份，一年之后，eBay再次大手笔投入1.8亿美元入主易趣，至此易趣彻底变成eBay易趣――一个电子商务市场巨头的中国子公司，拥有90%的市场，是不折不扣的大象。 <br />
<br />
　　淘宝创立于2003年，是易趣创立4年之后。一开始淘宝甚至没能列入eBay易趣广告投放的排斥性名单，后来才引起了eBay易趣的警觉。用马云的话来说，他们之间的差异就是蚂蚁和大象的差别。 3年后，即2006年，eBay易趣的市场份额下降到20%，淘宝上升至72%，年底的时候，被淘宝逼得步履维艰的eBay易趣宣布和TOM在线组成合资公司，TOM在线占股份51%，eBay占股份49%。至此，eBay以一种体面的形式退出市场，蚂蚁雄兵最终击败大象。 <br />
<br />
　　在个人即时通讯(Instant messaging)市场，中国企业也占据绝对优势。《2008年中国手机网民手机上网行为调研报告》显示，手机网民在各类移动增值业务(短信除外)中对移动IM的使用率最高。其中,手机QQ以80.8%的渗透率位居第一，飞信和移动MSN分居二三位。在移动商务市场，中国企业QQ拥有着绝对的话语权。<br />
<br />
　　在中国企业的即时通信市场上，中国企业也成为市场的主导力量，据2009年第三方统计数据，仅腾讯RTX就已占到超过70%的市场份额。 腾讯为代表的国产软件企业。它们的产品设计更加贴近中国用户的实际需求，产品操作更加贴近中国人的使用习惯，并亦具有很高的品牌知名度。<br />
<br />
　　Yahoo也在中国遭受惨败。雅虎中国网站在1999年9月正式开通，但最后雅虎不得不把自己在中国的品牌权和运营权卖给中国本土企业阿里巴巴，再加上10亿美金换取了阿里巴巴40%的股份。艾略咨询首席分析师、艾略研究院院长曹军波认为“业内公认中国雅虎是失败的，因为这么多年来，在业务上，用户数、运营收入和战略定位上都存在问题。”。<br />
<br />
　　大多数的业内人士都指出，西方那些有实力的网络公司在中国的失败的根本原因是缺乏对中国文化、网民需求、市场变化的了解。如果说在搜索引擎市场可能会受政治的影响，但在电子商务和即使通讯市场上的失败证明了政治并不是主要原因。即使搜索引擎市场受政治约束，但Google的失败是在和百度在同一市场用同样的政治态度竞争了几年后才不得不接受的事实。<br />
<br />
　　Google在中国的大失败需要给投资人以解释<br />
<br />
　　Google不是国营企业，而是一家是上市公司。公司的管理阶层需要对董事会和投资人负责。Google在其它市场上的摧朽拉枯般的击败一个个的有力对手。但在中国市场遭受惨败确实是一件让Google公司的管理层极其难堪的事情。更何况中国是大家都看好的一个具有巨大现实意义，并且蕴藏巨大潜力的市场。<br />
<br />
　　从市场经营结果来看，Google.cn确实没有存在的理由。不仅需要大量投资，但在竞争对手的打击下，Google.cn毫无还手之力。关掉Google.cn是市场决定的。正如资深互联网分析师洪波所说：“Google在中国已经运营了4年多，这些投入是白费了。”<br />
<br />
　　不仅巨大的投入打了水漂，而且丧失一个巨大的市场，Google的管理层必须给投资人一个的解释。这是西方企业管理层的基本伦理道德。但不幸，这次中国政府做了Google公司管理层的替罪羊。<br />
<br />
　　如果中国的网络监察制度是在Google进入中国后新出来的一个制度，最后逼得Google无法经营而被迫退出中国市场，Google满腹怨气是情有可原的。只有在这种情况下Google才有资格以如此的高姿态发表如此高调的声明来占领道德制高点。但事实并非如此。<br />
<br />
　　首先，中国政府的网络监察制度并不是什么秘密。更重要的是这一制度在Google进入中国之前就存在。其次中国的实际情况是审查制度越来越宽松，网络上的言论也越来越开放。国内的群体事件也基本上能在网络上得以公开报道，新政策的出台也能被公开讨论。这个趋势是明显的。最明显的例子就是2009年的绿坝软件事件。这一新政策就在网络上被公开讨论，最后的结果是中国政府让步。其它群体事件如2008年的贵州省瓮安县的万人大骚乱、2009年湖北石首因一厨师非正常死亡而导致数万群众参与的骚乱、湖北烈女邓玉娇事件，这些基本上在网络上被公开地讨论，并且推动了事件朝正确方向解决。不用我一一举例，这类的例子已经很多了。这些例子无疑是中国网络监察制度走向宽容的见证。<br />
<br />
　　Google是在中国政府的网络监察制度更严格的时候进入中国市场，希望在中国市场分得一羹。在中国政府的网络监察制度越来越宽松的情况下退出市场，但站出来高调批评网络监察制度，这本身就是滑稽的，也是站不住脚的。至少表明Google和其声明中的道德是格格不入的。Google只不过是利用政治作为撤出中国市场的遁词。正如中国市场研究集团董事总经理小山（Shaun Rein）所说：“我想谷歌只是在找个体面的方式来撤出中国。谷歌在中国的业务并不好，根本就斗不过百度。”<br />
<br />
　　Google并没有反省其在中国的不争气和无所作为，如果不再摆出点政治公关言辞，怎么能够安抚本土之舆论呢？政治成了Google在中国大溃败的一块遮羞布。<br />
<br />
　　Google的双重标准证明其只在乎利益并不在乎道德<br />
<br />
　　按照Google自己的高调标准，Google本身是一个高举道德大旗的魔鬼。2008年印度22岁的IT专业人士Rahul Krishnakumar Vaid因为在Orkut网站写下“我恨索尼娅甘地（I hate Sonia Gandhi）”而遭逮捕。Orkut是Google在印度的一个社交网络网站。Google立即向印度警方提供了Vaid的Gmail电子邮件信息。Vaid被起诉了违反了印度刑法第292条和信息技术法第67条，在网上社区发布辱骂印度国大党领袖索尼娅甘地的内容。如果被判有罪，他将可能要入狱5年，罚款10万卢比。Google声称：Google致力于保护用户隐私，但Google必须遵守所在国的法律。<br />
<br />
　　这还并不是第一起印度政府在Google帮助下逮捕其公民的事件。2007年11月也有一位名叫Lakshmana Kailas K的网民也是在Google的Orkut网站上张贴了一张亵渎印度教圣人Shivaji的图像，印度官方要求Google提供该网民的IP地址，Google完全配合，这位印度网民随后被印度政府逮捕，并遭受殴打。更为不人道的是该网民在印度监狱里被强迫用同一个碗吃饭和撒尿。更出乎预料的是事后证明该网民是被冤枉的。<br />
<br />
　　Google关闭在华业务对中国网民影响有限<br />
<br />
　　正如前面述及，中国网民上Google其实主要有两个途径。一个是中文站点Google.cn，另外一个就是Google的全球网站Google.com。而后者才是中国网民进入Google网站的主要途径。Google.cn本身对中国网民无关紧要。即使Google退出中国市场，其实也就是关闭Google.cn，再多就是关闭在华研发。这对网民影响并不明显。更不是有些文章恐吓说：Google关闭中国网站会让中国网民孤立于互联网之外。<br />
<br />
　　新华都集团总裁兼CEO唐骏说：“这对中国网民是无所谓的，但这将是他们做出的历史上最蠢的决定，放弃中国等于放弃半个未来世界！”<br />
<br />
　　当然说一点影响都没有也是不可能的。至少还有极少部分中国网民在用Google.cn网站。收到波及Google的Gmail服务业完全可能被中国政府切断。但Gmail的电邮服务的替代者多得数不胜数。<br />
<br />
　　总之，Google退出中国市场，只是退出在中国的经营。并不代表中国成为互联网的孤岛。<br />
<br />
　　网络监管中国政府并不是唯一一家<br />
<br />
　　美国政府在911事件后出台爱国者法案。这个法案以防止恐怖主义的目的扩张了美国警察机关的权限。根据法案的内容，警察机关有权搜索电话、电子邮件通讯、医疗、财务和其他种类的记录；减少对于美国本土外国情报单位的限制；扩张美国财政部长的权限以控制、管理金融方面的流通活动，特别是针对与外国人士或政治体有关的金融活动；并加强警察和移民管理单位对于居留、驱逐被怀疑与恐怖主义有关的外籍人士的权力。这个法案也延伸了恐怖主义的定义，包括国内恐怖主义，扩大了警察机关可管理的活动范围。美国甚至允许不经许可，执法机关就能监视公民通讯。<br />
<br />
　　加拿大的个人信息及其电子文件法案本身就允许执法机关在法律许可范围内调查个人信息。2007年加拿大媒体报道，执法部门正在争取不经许可而进入公民私人信息的权力。同样在2007年，一条新的法案要求加拿大的互联网服务商和手机服务商在没有许可的情况下向警察即使提供用户的个人信息，包括姓名、地址、互联网地址等。<br />
<br />
　　根据加拿大CBC网站报道，英国、美国、澳大利亚、新西兰、德国和瑞典都有类似的法律条款。<br />
<br />
　　英国2000年的调查权力规范法案就明确要求互联网服务商安装相关系统来帮助执法机关追踪电子通讯。<br />
<br />
　　Google高调退出中国可能不单纯<br />
<br />
　　2010年1月7号，国务卿希拉里&#8226;克林顿(Hillary Clinton)在国务院请吃饭。这是一场小规模的晚宴，规模虽小，来客却都是通讯科技界的重量级人物。客人名单上有谷歌首席执行官埃里克&#8226;施密特 (Eric Schmidt)、Twitter联合创始人杰克&#8226;多尔西(Jack Dorsey)、微软首席研究与战略官克瑞格&#8226;蒙迪(Craig Mundie)，以及Mobile Accord 总裁 James Eberhard, Cisco 的行销总裁Susan Bostron, 纽约大学教授 Clay Shirky, Personal 个人民主组织创始人Andrew Rasiej等。这是美国国务院利用信息技术来推进美国外交目标的努力的一部分。<br />
<br />
　　没过几天，在2010年的1月12日Google就跳出来。Google是否在为美国政府的政治目的服务呢？大家拭目以待。如果Google成为美国政府的政治打手，那中国政府对其政治约束是完全必要和合理的。</div>

							
							
							
							
