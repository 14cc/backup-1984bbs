													<h2>关于沙甸穆斯林的讨论我说两句</h2>
						
						
																											<div id="postmessage_342422" class="t_msgfont">以已知的信息判断，假如完美的处理沙甸问题是100分，我觉得当地穆斯林禁酒小组至少做到了70分。<br />
禁酒本身没什么好说的，有人认为应该禁，有人认为不该禁。这不重要。真正的问题是，这种分歧没有答案，而持两种标准的人要生活在一起，要交流要往来，谁也没办法把谁从肉体上灭了。那么在这种情况下，利用能掌控的资源，采取最恰当的方式妥善处理，才是王道。禁酒小组如果真的持续了3年多，没闹出乱子，这说不定真是一种成功模式。<br />
<br />
若干人抓住一个细节或者一个原则不放，而不去看具体的事件和事件背景去做批判，有意思么？除了互相谩骂，我实在看不出能得出什么结果。<br />
绝对捍卫私有权，有道理，但这是英式传统，这话是托克维尔说的，不是我。中国传统里，家可能更开放，从来没听说有在中国误进某人的家被人砍死的，我也不相信这样能判这家主人无辜。你可以说我说的不对，这是大势所趋，没关系，我要说的是，很难相信沙甸的人，不管汉人还是回族人，是这么想问题的。<br />
而且我相信当地的汉人，如果他想混下去，他就得尊重当地大都数人的习惯，那么他就得吃点亏。谁没吃过亏啊。不吃这亏，就得吃大亏。这不是原则不原则的事。这是很现实的事情，我选择在这儿生活，而不是别处，因为我的投入产出在这儿最优化。显然没人强迫他们居住在当地。 入乡随俗，就似乎这么个道理。<br />
极端尊重民族习惯和宗教信仰，这也没办法，毕竟是混居，凭什么非得完全按你说的算，我也有基本人权和私有权。反之完全不尊重民族习惯和宗教信仰，道义上首先说不过去，就算不谈道义，这样做对于当地汉人也未见的有好处。敌对，暴力，恐慌，那的确跟黎巴嫩在内战时期差不多。<br />
而现实是，永远不可能有完美的解决方案。只能寻求一定的平衡，在大原则不违背的情况下，以本土化的情况，本土化的习惯去处理。假如坛子里禁酒小组的回复是真实的，确实这个项目执行了3年多，应该勉强算成功。<br />
有人说，这么执行还把警察扯进去不公平，这么说有点道理。那么是不是可以技术性解决，加个监管机制什么的，搞个当地议会，既保护当地多数穆斯林的权利，又能保护相对弱势的汉族人的权利？<br />
我想这不大可能。这种操作，超越了当地人的知识、能力和资源。 不仅投入产出比高，而且风险大，这显然不是我党能允许的。在这种情况下，能处理到这个份上，还是在30多年前发生过灭绝惨案的地方，不容易了。<br />
<br />
第二个问题是，看到若干帖子，热烈讨论。我真的忍不住想说，大概全世界上汉族是最没有能力去学习如何和其他民族相处的民族。看看汉族人待的地盘，要么是将当地人汉化，比如布衣族，比如苗裔，要么是将当地人屠杀，比如羌族，随之而来的是，不被汉化的就得躲，能力强点的，躲到半山腰去，能力差的，就得躲山顶去，资源分布图基本就是汉化程度图。<br />
回族人是例外，导致这种例外的原因可能很复杂，有内部史的，有中西交通史的，有人类学层面的，但这种例外带来的也是硬碰硬。因为汉族人当中不懂得尊重，没信仰，没灵魂，没底线的，实在不是什么稀有的事情。我这么说当然也极端了，我道歉，只是我也有点情绪，需要发泄一下吧。我还是很热爱汉族文化的。<br />
可是，就像西藏、新疆，显然并不能说都是别人的错吧。<br />
<br />
第三个问题是，这么些年看到太多批评伊斯兰教穆斯林的帖子，相当一部分是很有道理的，但也有相当一部分是很无聊的，只是为了满足一种需要，通过歧视另一个文明而彰显自己是个文明人，这倒不是汉族人的毛病，大概是全人类的通病。比如西亚的阿拉伯人，前些年提到中国会是，听说那儿的人吃老鼠。<br />
<br />
可是，大概越没出息的越没落的文明越喜欢这么干，也有这种需要。</div>

							
							
							
							
