2、一个世代居住在某地的族群，即便没有规定那个地域的每一分地都是某人的私产，但从【正常的人类伦理】来说，是这个族群、社区的公共用地。外来者既然要为了赚钱入别人的乡，只能接受别人的管理条例。<br />
<br />
你要认真扯起来，在公共地方人拥有的自由应该更多。房东不让你在他的屋子里养狗，但是不能阻止你在大马路遛狗。这同理于无烟餐厅内禁止吸烟，但老板不能禁止你在大马路吸烟。如果穆斯林房东连要求房客不在自己屋子吃猪肉的权利都没有，那么房客就更有自由在沙甸村的马路上吃猪肉。<br />
<br />
这种问题用法理扯是扯不清的。不可能一个白鬼子过来中国租了一片地，然后他就具有在上面做一切让中国人硌应事情的自由。如果拜猪肉教的蠢汉族人们想不通这个道理，甚至连入乡随俗的道理都不懂的话，他该去看医生，或者上智障儿学校补补课。<br />
<br />
3、众所周知，伟大的拜猪肉教的蠢汉们，还有吃狗的爱好。他们也因为这种风俗，得到了白鬼子给他们的dog eater这个雅号。在白鬼子的社区里，dog eater完全有在自己屋子里吃狗的自由。只不过这件事比起打飞机还要不能大庭广众下进行，打飞机尚且能跟人谈谈，吃狗这事儿，要是给邻居知道，他就只好等着收法院传票，或者有社区的居民在他家门口抗议。<br />
<br />
我热烈希望拜猪肉教的柿油十字军们，快点到世界各地触犯不同族群的禁忌，宣扬自己的自由。<br />
<br />
4、蠢汉们很不服气，自己想了个歪理，说要逼迫在汉族人区居住的地方强迫穆斯林吃大肉喝酒。他们觉得这样做很能伸张他们的正义。<br />
<br />
所以我告诉他们了，快去宗教局成立拜猪肉教，把自己社区的九成汉民拉入教，找几个砖家叫兽论证当地自古以来就有以吃猪肉为圣礼的传统。这样，他们完全有理由要求在当地居住的家家户户都要吃猪肉，因为不吃猪肉是他们的禁忌。<br />
<br />
只不过这些天天在嘴边抹柿油的人忘了，选择居住地本身就是人所拥有的自由。一个穆斯林如果知道有这种宗教和教徒聚居点，他们是不会选择这种地方来住的。这就跟一个有着正常脑子的汉族务工者，在行使自己选择沙甸村作为自己居住地自由的同时，就清楚意识到自己有遵循当地不吃猪肉传统的义务。<br />
<br />
这也是沙甸村提及的，只要求他们不在村内吃大肉和喝酒，在村外他们怎么做，不管。虽然这些穆斯林没有天天在嘴边抹柿油，但行为在我眼中比那些拜猪肉教蠢汉更懂自由。<br />
<br />
5、我提供个段子。基佬李维斯主演的魔鬼代言人里面，就有一个喜欢用活畜牲血的非裔被别人告了。按理说柿油党眼中热爱自由的美帝人不可能不知道尊重人家在私宅做什么都行的自由啊，可为什么会这样呢。当然，后来主角提供的解决办法，就是让非裔捏造说那是他们的宗教传统，他们宗教传统有获得尊重的权利。所以蠢汉们要真想让自己地盘上所有人都吃猪肉，赶紧去宗教局注册才是王道。<br />
<br />
6、如何汉化、逼迫、硌应沙甸人？<br />
<br />
不要租穆斯林房东的房子，在那里买私宅，并且在注册拜猪肉教成功后，在那里天天搞吃猪肉圣礼。是的，这时候你已经有权利维护自己在私宅里的吃猪肉权了。他们真要告你的话，也可以用维护宗教自由的幌子来将他们一军。这一招同样适用于你们在白鬼子社区注册Dog Eater教后使用。邻居再嫌你们硌应，伟大的美帝法律保护着你们。<br />
<br />
不过，很可能出现的情况就是，你们不被欢迎进入该社区的任何商铺与私人场所。无论是拜猪肉教成员，还是吃狗教成员，就连买个安全套，都要灰溜溜地跑去不认识自己的社区商店里买。<br />
<br />
各大论坛的各种曝光极品贴是怎么来的呢？极品贴所有主角的共性是，招了众怒还自以为有理。有些事情是无法在法理上解决的。你们非要到别人的地盘故意做一些硌应人的极品行为，就别抱怨人家嫌弃你。</div>

							
							
							
							
