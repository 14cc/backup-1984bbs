&quot;很多人都认为我们的党是一个最好的党，理由是它带领中国人走向富强，虽然有一些腐败与特权。他们老说没有共产党，你现在有饭吃吗？有教育权吗？<br />
首先我想说确实改革开放给中国带来了繁荣，但是我们往往忽视我们凭什么繁荣了。只不过在文革以及建国的前30年时间，我们因为共产党的折腾，给全体中国人带来了灾难，比如由于决策失误导致2000万人饿死归罪为自然灾害，还有不知道在干嘛的文革。由于改革开放，共产党放开了经营的限制，把生活，学习，致富的权利交给了国民，然后国民依靠自己的双手把生活变好。1978到现在，因为全球化的利益，以及我们作为人有自己的致富权利，才获得了生活水平的提高。改革开放的过程就是把属于全能主义的政府的权力下放给民间，由我们自己去想办法去更好的安排经营自己的生活。没有共产党，我们会生活得更好。<br />
还有没有共产党，中国还是帝国主义国家的殖民地，继续清朝的没落。但他们从来没有看到香港，日本，韩国的繁荣。<br />
现在，国有企业开始兼并私营企业，重新占领市场。这绝对是一种社会的退步。因为推动中国发展的进程中，民营企业是主力军，国有企业占领行政垄断的特权，对效率的提高没有兴趣。<br />
共产党都说，国有资源与国有企业属于人民。人民是谁？是你是我还是他呢？绝对是属于控制资源的特权阶级。&quot;<br />
<br />
<br />
社会发展经济进步是大趋势，但是也需要一个强而有力的领导。国民党代表的垄断资本主义给中国人民带来深重的灾难，而上世纪中期中国的资产阶级并不发达，也没有成熟的政党和建设国家的理论。共产党是我们当时的唯一选择，无论过去现在都没有更好的选项出现。<br />
你说的无政府主义的生产，能实现全面协调的发展么？即使同样有这种趋势，但是达到这种效果所付出的代价难道不会比在党领导下进行生产大得多么？</div>

							
							
							
							
