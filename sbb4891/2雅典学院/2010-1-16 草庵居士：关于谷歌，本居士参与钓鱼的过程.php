该文写于一月前，作为香港某中文杂志的稿件。<br />
<br />
在亚太人权基金会2009年颁奖会召开的前后时间里，本居士承受了来自民运及中共等方面的巨大压力，各种诋毁及污蔑倾天而降，某些无耻小人更四处扇风点火，无恶不为。民运中人大多均有所闻。<br />
<br />
无论是王军涛、还是魏京生都可能对本人产生诸多怀疑而放弃了参加会议的承诺，但洛杉矶的诸多民运弟兄了解并信任本人，特别是身处其他地区的一些民运领袖更是不顾诸多 “民运知名人士”的劝阻，冒着与某些“民运知名人士”绝交的风险赶来洛杉矶为本居士助阵。<br />
<br />
但是，由于洛杉矶民运诸弟兄坚定地站在了正义的一面，在面对威胁甚至是人身恐吓的情况下义无反顾的支持着本居士。忍辱负重，面对污蔑威胁不为所惧。<br />
<br />
在网络上，一群跳梁小丑不知好歹企图加大污蔑，转移视线，更是自爆奇丑。这些人以中共的口吻及民运的名义给被邀请前来的美国议员及美国政府机构发黑函污蔑亚太人权基金会，开始某些人并不承认，但最后在薛伟、潘晴、陈维明等人的压力下对该行为作了部分承认，某些人对此澄清是秉承某人旨意受托而为，并非自己行为。在网络上，更有某些人蒙面攻击污蔑，本居士曾善意并委婉劝告，但某些身在海外及少数混在民运中之丑陋之人仍不免被美国某些机构关注调查，甚至某些人已经被列入长期观察名单。<br />
<br />
好在时间与事实是最好的检验标准，当这次钓鱼行动完成之后，本居士仍是满心欢喜，看到美国政府及企业终于能以事实和正义面对中共强权，这就足以让本居士欣慰。<br />
<br />
在此，本居士再次感谢支持本居士的以刘因全、郑存柱、陶君、李大卫为首的洛杉矶众弟兄及美国东部王军、郑科学、倪育贤、澳洲以潘晴为首的全球各地的支持者。<br />
