<br />
中美网络间谍战的钓鱼行动<br />
<br />
美国总统奥巴马访问中国返回美国不久，美国国会中的美中经济与安全审查委员会于20009年11月19日公布年度报告，这个总页数高达367页的文件中列举了相当多的事实指控中国官方机构直接运用电脑技术进行网络间谍战。中国政府立即进行了反击。中国外交部发言人刘建超19日指出：“该委员会无视中国政治、经济、社会等各项事业发展进步的事实，固守偏见，公然干涉中国内政，对中国进行诬蔑攻击，企图误导舆论和公众，给中美在广泛领域的合作设置障碍，但他们的图谋是不会得逞的。我们已经向美方提出严正交涉，表示坚决反对。”<br />
<br />
网络间谍战是冷战结束之后新的一场战争，也是高科技战争，中国政府在面临着与美国及西方国家经济、军事弱势之后，是首先考虑的是如何使用其他方式战胜美国及 西方社会。喧嚣一时的超限战就成为中国政府及军事部门的首选战略。而网络间谍战也是中国军方超限战略中的一个重要手段。美国及西方社会面对中共这种不择手 段的超限战也是积极应对，包括美军成立网络战略指挥部等，郑重其事地将网络战列位美国重大军事战略。<br />
<br />
网络战进行于无声处，但如何掌握对方官方介入本国网络从事间谍活动，并掌握证据这就成为了整个网络战争中的一个重要任务。<br />
<br />
2009年1月，总部设立于美国洛杉矶的美国亚太人权基金会刚刚搬入一栋完工不久的办公大楼，就在该基金会迁入半个月之后，该基金会发生被盗事件。两台不与网络连接，只存储资料的台式电脑被盗。奇妙的是，同样的办公室里面，另两台价值高，功能全的电脑及室内的40英寸的平板电视机等诸多设备并未被盗，未上锁的某义工办公桌内的近千元美元现钞也未被盗。<br />
<br />
亚太人权基金会发现被盗后马上通知美国警方及联邦调查局（FBI）， 美国调查机构迅速派出刑侦技术人员勘察，发现此次偷盗非常专业，几乎没有留下任何痕迹。而且大家对盗贼为何放弃高价值产品，而只偷盗两台廉价电脑而奇怪。 难道盗贼的目标只是为了亚太人权基金会两台专门储存资料的电脑而来，如果是这个目的，那么，盗贼只有是视亚太人权基金会为敌对势力的中国政府。<br />
<br />
基于这个不成熟的判断，美国调查机构对亚太人权基金会的设备及各方面进行了调查，结果发现，该基金会数台电脑及基金会负责人的电脑及电子通讯信箱均被人恶意 设置木马，部分信箱的信件被转移到了某个特定的信箱。换句话说，亚太人权基金会的某些电脑已经被人远程遥控，遥控的人可以随意进入这些电脑查看资料。<br />
<br />
美国调查人员在研究个分析之后，决定采取钓鱼行动，通过这些“木鸡”电脑调查了解操纵使用这些电脑的幕后人员。经过精心策划，一场中美之间的网络间谍大战开始了序幕。<br />
<br />
亚太人人权基金会致力于推动中国及太平洋周边国家的人权活动，多次奖励中国的政治异议人士及人权活动家，这很有可能就促成了中共对其的监视及敌视。2009年， 亚太人权基金会首先在美国国会及联邦政府提出了人权议案重返中美经济的活动。特别是积极支持美国工会组织提案制裁中国产品，这些情况都足以引 起中共情报机构的关注。而近期亚太人权基金会为推动美国联邦政府制裁中国橡胶轮胎和石油钢管而积极筹备的人权听证会更会成为中共关注的重点。<br />
<br />
很快，美国调查机构在亚太人权基金会的配合下，将一份虚构的邀请大陆劳工人员听证的名单放入被植入木马程序的亚太人权基金会电脑中，这台电脑正常的运行，而被盗转的电子信箱也正常运行。为了真实性，亚太人权基金会也正式给部分邀请参加听证的中国劳工发出邀请信，同时也派出一位美国籍的亚太人权基金会义工前往中国，“协助参加听证的中国劳工申请签证”。<br />
<br />
2009年5月9日，美籍义工甄女士到达中国广州，随即便发现了跟踪的中国国家安全部人员。尽管甄女士在中国未从事任何违法行动，只是到处观光及前往美国使馆。但是，2009年5月15日，中国北京时间晚上5：30分左右，美籍公民甄女士在广东省江门市被中共国安部警察抓捕，罪名是“企图偷渡中国国境”，这个罪名令美国政府哭笑不得，这世界上竟然有持美国护照合法进入中国 而需要偷渡离开中国的事情吗？。在美国联邦政府及美国驻中国大使馆、广州领事馆馆员的即时救助下，甄女士被关押一天之后，于2009年5月16日释放，但被软禁在广州宾馆，她的美国护照被中共警察扣押，直到2009年5月19日才在美国政府的强烈外交压力之下 ，及美国国会议长南西女士即将访问中国之时才交还给甄女士，并将甄女士驱除出中国。<br />
<br />
甄女士的这一试探行动证明了中共是对亚太人权基金会电脑木马的真正背后黑手，因为甄女士既非中国大陆政治敏感人士，也非在美国的政治、人权活跃人士，在前往 大陆之前只是一名普通的家庭主妇。另外一个原因是，甄女士前往中国之前，并未有其他人知道，但是其前往的行程计划却放置在已经设有木马程序的亚太基金会电脑之中。也就是说，只有能看到这部用于钓鱼的电脑里面的内容，中共才能清楚地知道甄女士的行程和目的。通过甄女士的行动才能确定谁是这台木马电脑的真正操控者，也只有通过甄女士的行动才能确认中共是这次网络间谍的真正主谋。<br />
<br />
很显然，中共对在中国访问的甄女士采取的行动非常明显地证明了中国政府确实是在美国机构电脑中采取网络间谍的真正黑手，但如何证明并让中共官方确认这是另一个重要的事情。<br />
<br />
亚太人权基金会与美国调查机构积极配合，采取了各种方式试图让中共承认其直接插手网络间谍案件。在此期间，部分中共海外间谍及中共国内的情报机构纷纷释放各种信息，试图掩盖真相。但是，亚太人权基金会与美国调查机构通力合作之后，中共首次以某种方式承认了中共政府官方直接插手网络间谍案件，这让美国政府直接 抓住了中共赤裸裸的对美国及西方国家的政府与机构采取非法手段进行网络间谍的证据。<br />
<br />
2009年8月3日，中国政府官方媒体新华网刊登了一条消息《有人为敛财教唆造假申请签证》， 文中伪称某美国公民之妻受骗，该文提及的某美国公民之妻正是亚太人权基金会和美国调查机构为查证网络间谍案设置的“鱼饵”。按照美国移民法律，美国公民的合法妻子是美国政府规定的第一优先移民，没有任何限制，根本就不需要伪造任何文件。而该报道真实的目的实际上是想恐吓中国百姓，阻止中共国百姓参加美国人权听证，但未承想该报道却一下就误中美国调查机构设立的陷阱，从而证明了中国官方机构涉及并主导网络间谍案件，使用非法手段入侵美国机构及组织的电脑，窃取美国机构及组织的机密。<br />
<br />
非常有趣的是，该报道中提及一名名为晓琴的学生，该报道称：“晓琴为提高英语水平，考取高级护师资格证，今年 2月让朋友在美国帮忙联系学校，并就申请赴美手续问题咨询过美国几家律师事务所和移民公司，且留下了个人信息。不久前，晓琴从有关途径获悉，自己的名字竟出现在“亚太人权基金会”在境外举行敏感活动的人员名单中。晓琴表示，从未通过任何非法途径申请签证，怀疑有人用自己的个人资料通过网络发布了不良信息。本应作为隐私的个人资料为何被用于为境外敏感活动造势？晓琴非常气愤。”<br />
<br />
这份报道非常明确的提到了“晓琴从有关途径获悉”。事实上，晓琴的资料只是亚太人权基金会电脑中的一个“鱼饵”，该晓琴女士既没有获得过亚太人权基金会的邀 请，亚太人权基金会更没有与她有任何联系。她不过是出现在亚太人权基金会资料电脑中的一个虚拟人物，一个美国调查部门虚拟的“鱼饵”。那么，既然是个虚拟 人物，这名晓琴女士又是如何“从有关途径获悉”其名字被列入“亚太人权基金会在境外举行敏感活动人员名单中”，这个有关途径是什么？这个有关途径又是如何 获悉晓琴这个虚拟的活动名单？很显然，这个有关途径是中国官方机构，而这个有关途径是采取非法的网络间谍手段获取了亚太人权基金会与美国调查机构共同配合 而虚拟的一个敏感名单。根据中国广东省公安厅官方网站2009年8月5日的新闻记录，这个有关途径就是中国公安部和国家安全部，具体的执行机构是中国公安部广东省公安厅及中国国家安全部及广东省国家安全厅。自此，中共不打自招地承认了中国官方机构通过网络间谍窃取美国机构及组织的卑劣行径。<br />
<br />
网络间谍战已经在中美之间展开多时，中国政府已经面临众多国家的指控。这些国家不仅包括美国，还包括德国、英国、法国、日本、韩国等众多的国家，面对众多国 家的指控，中共一贯的手法是不承认。由于网络技术的问题，西方国家虽然有明确的证据，但依然无法获得更直接的证明。但在这次中美之间的网络间谍战中，中共 政府机构“误”中美国陷阱，不打自招地以官方角度承认了其政府机构直接涉及主导网络间谍案，这也就直接促成了美国国会形成议案文件。美国网络间谍案的成功 也鼓舞了全球其他西方国家对中共的警惕，各种网络间谍交锋日益激烈。</div>

							
							
							
							
