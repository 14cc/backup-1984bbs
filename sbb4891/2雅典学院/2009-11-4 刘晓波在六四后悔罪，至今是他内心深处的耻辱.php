幽篁 发表于 2004-9-18 0:57:00<br />
<br />
刘晓波最初给我的印象是个职业革命家，时不时反动着，时不时被人民专政着。凡有个风吹草动，就可以听到他义正辞严的声音。由于骨子里的不安分，我比较兴灾乐祸地读他的文章，几乎不错过一篇。喜欢他文章另一个原因是喜欢他文字的韵味和节奏感，这让他的文章有别于那些仅剩光秃秃愤怒咆哮的文字。<br />
从他的《恐怖对人性的摧残――狱中读&lt;1957年的夏季&gt;》一文开始，我对刘晓波的关注从字面移到了文字背后的人。这是他读朱正所著《1957年的夏季》的读后感。文章中，他不仅对各类人在运动前后的表现作了反思和评点，文章末尾更对自我作了深刻的剖析：<br />
<br />
<strong>读到反右中那些被打成右派的人在大会上做检讨，自我虐待，自己往自己的心中扎刀子，我真是无以言对，也没有任何事后明白的优越感，反而对他们有一种同情的理解。因为我想起89年自己在秦城的内心经历：最后的悔罪。如果说，在57年那种大环境之下，那些右派的自我作贱还可以理解和可以原谅的话，那么我在90年的忏罪就是不可自我原谅和自我赦免之罪。它是我生命中永远的耻辱，永恒的罪责，纵令我以全部生命去洗刷，也无法干净了。虽然我的妻子从未问起过这件事，也未对此事有过任何表示，但我似乎看到了我的悔罪在她心中留下的阴影。她从不提及此事，也许是深恐伤我，因为这是我一生中最下贱的行为，那伤口永远不会愈合，永远新鲜，稍一碰就会血光四溅。她怎么可能去触碰呢？<br />
他的自我剖析使全文不单停留于批评别人，而有了自我反省的凝重。一如他说的：“道德苛责只能用于自己，而不能施之于他人！”</strong><br />
<br />
我很喜欢他的另一篇文章是《心牢中的女人》，非常感性的文字描述了坚守在大墙外的那些女性的勇气、无助和无奈：<br />
王英绕着看守所的高墙边走边呼喊徐伟的名字，一声声、一遍遍，常常喊着喊着就哽咽的喊不出声来。即便这样，她还要喊，她相信深深相爱的人会“心有灵犀一点通”，相信徐伟能听到她的呼唤，知道她是多么想他念他为他寝食难安。……王英仍然不放弃，仍然去呼唤徐伟，每一次她都是怀着幻想而去，带着失望甚至绝望而回。可以想象，当她一个人托着疲惫的身子和喊哑了的喉咙回家时，那种在深更半夜里独自行走时的沉重，那种只有路灯下的影子相伴的孤独无助，使人窒息。<br />
可能更多人喜欢的是刘晓波的政论，也许是因为人心某处爱的荒芜，这篇文章我贴出后点击率终不如他随意写的一篇政论。他的诗歌和散文也不能引来他政论这样多的热情读者。我却始终偏爱他文学性的文字。 <br />
<br />
通过网络和他有了联系，我才知道他是个文艺学博士，难怪他的文章文笔好。我说我怎么就这么眼光独到呢！刘晓波的声音柔和，有点结巴，可在说到关键处总能适时地停顿，避开险象。最惊人的是一次在电话里读篇长达六千字的文章给我听，语速快还流畅无比，尽管文章有大段的拗口的排比句和西文诗句。欺负他在电话里看不到我的表情，我笑得东倒西歪。<br />
他深知自己处境的特殊，和我从来不涉及敏感话题，谈得多的就是他的新学术文章和对文学的感受。他有好文章也是先发给我，不会自己去贴到网站，怕会为网站带来麻烦。有次他被禁锢很久后终于可以上网了，他发给我的第信只有一句话：“看到网站还在真好！”我也因此被他的细致和关心而感动。<br />
一度网上一些网友频频出事，我困惑这种行为的意义，就问刘晓波对自己的所作所为是如何考虑的。他真可以算是出事的模范了。<br />
他告诉我，他的出发点极为个人的：在中国这样的有辱尊严且鼓励说谎的制度下，做个有尊严的诚实的个人。生命中总要选择，不选择为尊严和诚实付代价，就要为苟且和说谎付代价。在他能力的范围内，会为亲人着想。他做的一切，他的妻子全部知道，他俩秉持相同的信念和做人原则。他不想做那种只为自己的社会形象而勇敢到”自私”的英雄，并讨厌那样的英雄！ <br />
<br />
刘晓波喜欢茨维塔耶娃，因为她说：“我不喜欢海，因为那么大的地方，却不能行走！”刘晓波不喜欢现在的中国是不是因为那么大的地方却没有他行走的自由？<br />
