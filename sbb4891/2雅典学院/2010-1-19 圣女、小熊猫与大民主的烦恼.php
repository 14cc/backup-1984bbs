													<h2>圣女、小熊猫与大民主的烦恼</h2>
						
						
																											<div id="postmessage_441226" class="t_msgfont">看了1984上几个关于民主词汇、概念的相关帖子，争论不休，费力不少，却收获甚少。词义和概念的混乱令人堪忧，众说纷纭，没有一个共识的是非标准，可能是受毛毒之害，在民主的概念上也搞“作风民主”，而拒绝“统一思想”，仿佛在牛顿三定律问题上搞统一认识也属于“土共”作风似的。好吧，社会人文学范畴，政治概念没有绝对真理，（唉～相对真理总还是有的吧？）百家争鸣，我在这里就说说我的一家之言：<br />
民主是一整套社会制度，浮在水面上能看见的是“投票选举和罢免”，而在水面下还有基础的条件部分。<br />
这是来自于西方的文明，我们在争论或学习的时候，应该谦虚地找来西方的著作和文献作为是与否的标准。<br />
好了，如果有人觉得这样不舒服，不适应，做不到，要自己定义。那也可以，但自己重新定义的话，至少要尽可能遵守汉语语言语法、词汇方法和语义逻辑，要便于结合或应用于实际问题。换句话说，要能够自圆其说。<br />
比如，咱不能在定义了民主的内涵与外延之后，出现“一会说文革是反民主的，一会说文革是大民主的”情况，如此混乱和矛盾，只能说是民主的定义出了问题。<br />
制造名词，定义概念，最好避免出现大熊猫是熊猫，小熊猫不是熊猫的尴尬事例，为什么我们要给自己找麻烦呢？政治概念，社会人文范畴的问题本来就很复杂，我们能不能严谨一点，避免用大驴非驴（骡子？），白马非马（笑话？）这样的思维去解决问题。<br />
有些人说“民主”不是什么圣女容不得玷污，该是什么是什么，文革有“民主”特征有什么不可以。这不还是民主的定义有问题么？<br />
我们追求和渴望民主社会、民主制度，你非要把文革也作为“民主”塞到我们的追求目标里，这不仍然是民主的定义有问题么？<br />
我们从汉语字面上分不清民主究竟怎么量大小，是论个头还是论重量，你非要说“大民主”也是民主，或者说“大民主”不是民主，如此混乱不堪，怎么能保证今后不会出现小民主、瘦民主是民主或不是民主的扯淡争论呢？<br />
我们没有想把民主的概念鼓捣成圣洁的女神这种欲望和必要，但是总不能把民主操弄的跟个妓女一样吧？<br />
所以，我个人以为，为了保持头脑清醒起见，讨论民主的时候，你把西方人文课本打开，谦虚一点死不了祖宗。要么你骄傲，你自己定义，拜托定义得严谨得体，简易清晰。让人一望可知其意。OK?</div>

							
							
							
							
