																											<div id="postmessage_847775" class="t_msgfont">民主（或译民主制、民主主义）从其字面上来看（源于希腊文δημο&#962; demos；人民；κρατειν kratein；统治），代表着由人民统治。至于民主的统治方法、以及其“人民”的构成范围则有许多不同的定义，但一般的原则是由多数进行统治。民主通常被人与寡头政治和独裁政治相比较，在这两种制度下政治权力高度集中于少数人手上，而没有如民主政治一般由广大人民控制。民主一词经常被使用于描述国家的政治，但民主的原则也适用于其他有着统治行为存在的领域。<br />
<br />
民主的形式<br />
<br />
直接民主―又称为纯粹民主[1]，是一种人们直接投票决定政府政策的制度，例如决定是否接受或废除某种法案。之所以称为直接是因为决策的权力直接由人们所行使，而不经过任何媒介或代表。在历史上，这种形式的政府相当少见，因为在实践上要将人们全部聚集起来投票相当困难，所花费的成本和时间都非常高昂。所有直接民主都属于较小型的共同体，例如城邦。最值得注意的是古代的雅典民主。<br />
<br />
代议民主则是较常被采用的制度，之所以称为代议制是因为人们并非直接投票决定政府政策，而是选出民意代表来参与政府实体或议会。民意代表可能是由全体选民选出（如比例代表制）或代表特定的区域（通常是依据地理划分的选举区），一些制度则混合了这两种方式。许多代议民主制也结合了一些直接民主的成分，例如公民投票。<br />
<br />
许多实行社会主义制度的国家也声称拥有民主，例如（前）德意志民主共和国、朝鲜民主主义人民共和国和中华人民共和国。马克思列宁主义者认为民主应是由无产阶级对人民群众的民主和对敌人的专政的统一，即“无产阶级专政”，例如中华人民共和国的人民民主专政政治体制。<br />
<br />
在今天，民主通常用以称呼代议民主制的其中一种形式―自由民主制，在自由民主制里被选出的民意代表、以及多数人的民意在行使决策权力时必须受到法治的限制，通常是用一套宪法加以管制，以强调对于个人和少数派的自由和权利的保护。这种形式的政府在近年来越来越常见，目前世界上将近一半的人口都生活在自由民主制政权之下[2]。<br />
<br />
相反的，非自由民主制则用以形容那些“有民主”、却没有保障自由的制度。这在许多后苏联的国家相当常见，不过这种现象并非最近才出现的。</div>

							
							
							
							
