																											<div id="postmessage_459770" class="t_msgfont">秋风：谁在反对孔子<br />
??作者：秋风<br />
??《孔子》电影上映，照样引来激烈争议。尤其是，据说，此剧上演之际，电影院要停止放映2D《阿凡达》。很多人恨屋及乌，《孔子》究竟如何，尚未揭晓，很多人对孔子已平添恶感。导演的霸道和肤浅，也同样给孔子增添了霉头。<br />
??当然，这种恶感其来有自。现代中国的文化史，基本上就是一部反孔史。唯在不同时期，反孔的理由有所不同，反孔者的构成也有所不同。汇集于今日，烦孔、反孔群体就呈现出光怪陆离的景色。<br />
??最响亮的反孔说法，发自已有近百年历史的新文化运动。陈独秀、胡适等人的主要看法是：孔子没有自由、平等的观念，孔子又被独尊为文化、思想之霸权。这套价值观禁锢了国人心灵，妨碍中国文明的进步。中国要现代化，必须打倒孔家店。<br />
??这种说法回荡在过去一个世纪的历史深处。过去二三十年，新文化运动被神话化，自认为信奉自由价值的人们大多都表现出神往那个时代的姿态，当然也就欣然接受了其反传统、反孔子的理念。<br />
??反对孔子的第二种说法同样源远流长。就在新文化运动前后，社会上出现过 “军国民”运动，倡导尚武精神，而孔子及儒家又遭到抨击，因为它被当成压制尚武精神的障碍。这种反孔理由曾沉寂几十年，过去十年又复活。中国实力增强了，受迫害的民族主义想象开始转换，具有一定进攻性。这种情绪强烈的人立刻发现孔子是坏人。他们认为，现在的中国人必须有尚武精神，为此必须倒孔。<br />
??平心而论，这两种反孔的理据，并非全无道理：秦以后的儒家确实存在反自由、抑制人的生命之健全力量的倾向。但只要在智力上不偷懒，只要没有被寻找替罪羊的隐秘心理所控制，其实完全可以把孔子与秦以后的儒家分开，把继承了孔子精神的正宗儒家与那些曲学阿世的俗儒分开。<br />
??进行种种区分的关键是正确地理解周秦之变的性质。在前面论及中国早期历史时，我反复说过这样一句话：后世儒家很难理解某某制度，或很难理解孔子某句话的含义。我的这个论断，基于一个最基本的事实：孔子生活在封建制下，而战国时代经历巨变，从秦代始中国就建立了君主-官僚-郡县制。<br />
??制度会影响人们思考问题的方式和想象世界的方式。在秦以后的漫长历史中，儒家士人缺乏进行制度比较的条件，他们思考治理问题、想象世界的方式就逐渐被局限在君主-官僚-郡县制框架中，不自觉地以此制度作为范式想象封建制度，诠释孔子思想。<br />
??比如，孔子生活在封建时代，按当时惯例，他作为“士”，有随侍领主出兵打仗的义务。孔子既是文士，也是武士。在古代孔子画像中，他也是佩剑的。孔子也向弟子们提供“射、御”训练，这是当时的士必须进行的车战技术训练。但秦以后历朝均刻意地防范民众习武，在这种环境下，儒生也就越来越文弱，变成了纯粹的文士，甚而厌恶健全的生命力――― 这一点构成了儒生与英格兰绅士的重大区别，尽管儒生也是一种类型的绅士。<br />
??孔子的自由精神同样存在着被经学遮蔽的倾向。孔子生活在封建时代，他享有自己的权利，在这个权利范围内他是自由的。孔子把自由作为一个自然而然的事实，在此预设基础上进一步讨论尊严、平等、仁爱等问题。后世儒生却生活在日益逼仄的政治环境中，渐渐不知自由为何物，孔子享有而未说出的自由，就被他们忽视，高明者一门心思讨论内心的仁爱，低劣者则把礼当成僵固的规则强制他人。<br />
??如果具有正确的历史知识，那今人不难重新发现孔子的思想及其价值。但当下的反孔者，假如年纪在四十岁以上，他人生中的一个经历必对其反孔情绪、理念的形成产生了重大情感影响。这就是上世纪七十年代的批林批孔运动。今日反孔的网络言论中可以清楚看到这场反文化运动的遗存：有人一张口就说，孔子是奴隶主的代表，阻碍历史进步云云。<br />
??这场反文化运动的情感影响非常深刻，不经过同样深刻的反思，由此形成的情绪是很难改变的。比如，很多自认为信仰自由和尊严的人那里，就可以看到一种奇异的思维断裂：他们坚定反孔，却忘记了，就在孔子遭到全民声讨的时代，国人毫无自由。</div>

							
							
							
							
