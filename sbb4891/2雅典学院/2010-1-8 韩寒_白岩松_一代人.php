													<h2>韩寒/白岩松/一代人</h2>
						
						
																											<div id="postmessage_421574" class="t_msgfont">刘原博客<br />
&nbsp; &nbsp;&nbsp; &nbsp; <a href="http://sohuliuyuan.blog.sohu.com/141655484.html#comment" target="_blank">http://sohuliuyuan.blog.sohu.com/141655484.html#comment</a><br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;&nbsp; &nbsp;先推荐一篇李海鹏写的《韩寒者，冒犯也》，刊于南方周末新年第一期。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;韩寒刚出道时，我就知道他了。很长一段时间里，我没怎么注意他，当时他还是小毛孩，我觉得再有才气也是有限的，等他成熟了我再去看他。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;我在北京那两年里，韩寒忽然再次暴红，各大报纸的娱乐版经常炒作他和徐静蕾如何如何，坦率地说，当时我不喜欢他，甚至有些下意识地排斥，我知道并理解大众需要娱乐，但我很难喜欢一个整天上娱乐版的人。此间韩寒开始对文坛发炮，这个还有点意思，文联作协是很无聊的计划经济时代产物，苏联过来的舶来品，年轻人轰它一炮是应该的。不过我依旧觉得，去轰炸式微已久的作协，格局还是小了；做法虽然对，但于社会的意义还是不大。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;真正让我喜欢并尊敬的，是这两年里的韩寒。他开始关注公共领域，开始拔刀。刀刀见血，实在让人赞叹。我赞叹的不单是他的聪明和才华――现在的许多年轻人都很聪明，但都是小聪明，搞搞钱爬爬官还行，大聪明、大智慧是丝毫没有的，随便一忽悠就去ANTI这个ANTI那个了，被人卖了还帮人数钱，但韩寒非常地清醒，他有非常好的价值观，看问题很深刻，三言两语就把事情的本质说清楚了。而且，他有勇气，对分寸拿捏得也好，我知道肯定有些人对他恨之入骨，但还真拿不出什么致命的理由做掉他。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;我不主张偶像崇拜，但这个时代确实需要韩寒这样的偶像人物，一些有担当、有良心的媒体对韩寒给予高度评价，就是通过韩寒告诉无数年轻人：真正的公民，真正符合时代潮流的年轻人，真正的个性，是韩寒这样的。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;当更多的韩寒出现，这个国度将非常之美好。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;我前不久和连岳围炉喝酒时，曾聊过我的一个疑问：我们这代人，从小就是被洗脑的，后来读了大量的史，接受了大量的启蒙，接触了无数的社会真相，才艰难地蜕变成有正确价值观的人，而韩寒显然读的史不太多，也没有我们这些新闻人的从业经历，年纪也轻，怎么忽然就和我们的思维完全一样了？太不可思议了。我们长年累月才达到的，他似乎一夜之间就做到了。连岳的解释是：韩寒懂得从简单出发，从常识出发。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;韩寒还是值得期待的，我并不认为他已经到达了他的巅峰，他的头顶还有无限的天空。韩寒的文字并不是完美无缺的，如果加深历史底蕴，文字技巧上更纯熟一些，那就更好了――这也是我不赞同把他神话的原因。但我旋即又想，韩寒其实不需要这些，他不需要背负别人的期望而行，他独立，快乐，这就足够了。我们不要把任何东西强加给韩寒，这个年轻人的发挥会超越我们的想象。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;我曾担心媒体的盛赞会捧杀韩寒，但现在不担心了，这小子是个外星人，没人杀得了他。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;我对韩寒的观感的改变，和对白岩松的观感的改变极其类似。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;先推荐一篇《白岩松：为大家去触碰底线在哪里》，南都周刊的稿子。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;许多年前，我是个桀骜青年。我不喜欢白岩松，他的诗意语言偶尔听听还不错，但听多了就反感了。当时我已经进入新闻行业，已经开始独立地思考。我不喜欢为一些明显丑陋的事物涂脂抹粉，尽管我能体谅白岩松，但非常之排斥。我讨厌歌功颂德。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;10年前，也就是2000年，白岩松到广西签售他那本《痛并快乐着》，期间到一家报社接读者热线，一个瘦削的年轻记者很尖锐地当面问他：广西的大贪官成克杰几年前上过你们的《东方之子》，画面是他站在冲锋舟上指挥抗洪，形象非常光辉，请问你有何看法？<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;假如白岩松看到我这篇博客，不知他是否还能记起这桩旧事。那个小记者是我。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;我10年前看过《痛并快乐着》，看过他写他的1989。我知道，他骨子里跟我们这些新闻人是一样的，我亦知道，他说着许多违心的话，CCTV不是一个能说真话的地方。但是，作为同行，我理解他，却不能喜欢他。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;许多年间，我不注意白岩松，偶尔看到他，我会懒洋洋地换台。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;几个月前，我在网上看到一个视频，白岩松喝醉了，满脸通红地上节目掰，完全是酒话。他不再伪装了，他不再抒情了。那一霎，我时隔10多年后，重新喜欢上了他。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;他在那个节目里，一些话已经向底线逼近。从CCTV的角度，无疑是直播的重大事故。但我知道，真正的白岩松，是这一个醉鬼。我看到了他的可爱。我看到了他的内心。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;我注意到他曾在2008年反对抵制家乐福。我注意到他在2009年说自己要开始说真话了。的确，他开始这么做了。真正的白岩松回来了。我喜欢，并且希望他继续。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;白岩松，柴静，让我们发现CCTV还是有值得尊敬的人的。希望CCTV的高层不要为难他们。这两个人以自己的新闻良知，担负着CCTV的最后一丝颜面。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;知名度许多时候未必是个好东西，周正龙够出名了，也就一烂人。只有当知名度用于推动社会进步时，才会是个好东西。韩寒和白岩松未必是最完美的，但他们以自己的知名度在推动社会的开放和进步，理所当然应该获得媒体的嘉许。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;中国最需要的，是启蒙。已经启蒙的人有责任去启蒙更多的人，令社会向良性的方向扭转。我们知道现实有多糟糕，我们必须改变，否则迟早祸及我们每个人。我们都应该做一些不计回报的事情，生活会给我们更大回馈。这是我和许多朋友坚持写看起来毫无收益的博客的原因。今天看到一条新闻，说70年代正在迈上政治舞台，用不了多久，就是80后了。我自己跟官场无甚关系，亦志不在此，但我相信，看过我文字的许多年轻读者将来会非常优秀，出色，他们之中肯定有人要踏进舞台，我希望我的文字能多少影响他们，至少懂得尊重人性，尊重人权，至少要明白常识，至少不要盘剥压榨百姓。这也是为文者最微薄的一点希望。我曾在一个官方饭局上，看到一位新闻界前辈对一帮年轻的团派官僚苦口婆心地教诲：你们将来是要走上更高岗位的，要多学经济，将来要学会善待媒体。我不知道那些官崽听进去没有，但我当时很感动，我们这代人要把这样的事情继续做下去，所谓改良，大抵如此。<br />
<br />
