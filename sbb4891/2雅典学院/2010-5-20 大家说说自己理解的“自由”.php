													<h2>自由，有两个层次</h2>
						
						
																											<div id="postmessage_628904" class="t_msgfont">第一个层次是可能达成共识的，也就是一般意义上的自由。<br />
比方说：允许你出国，就比不允许你出国自由；允许你批评宗教，就比不允许你批评宗教自由；允许你不服兵役，就比不允许你不服兵役自由；等等。在此基础上我们再就事论事地探讨这些自由如何互相权衡、实现最大化。<br />
一般意义上的自由，当然也有截然相反的意见。比方说宗教信仰自由，某些人就会理解为：他有禁止你不信仰他信仰的那个宗教的自由。再比方说兵役问题，美国的某些反战分子现在竟然认为公民有义务服兵役制的自由（他们想通过义务兵役制绑架国民，以达到反战的目的，如果是雇佣兵役制，都是各人自己选择的职业，无论士兵还是家属，就不那么反战了）。等等。<br />
这种截然相反的意见，其实质只是：嘴长在他身上，他有拿嘴放屁的自由，就算他说自己的爸爸是驴，妈妈是马，只要他爸爸妈妈不去告他诽谤，那也是他的言论自由。是不值得深思的。<br />
<br />
第二个层次则是很难达成共识的，近乎哲学或宗教范畴上的自由。<br />
比方宿命论的一些认识（人是由本能控制的不自由的动物等），等等。这个就不能指望达成共识，只能跟有兴趣的人去探讨。<br />
对于这个层次，当然见仁见智了。唯一需要注意的是，这个层次不能达成共识，不等于在前一个层次不能达成共识；你不能故意用讨论后一个层次的方式，去给讨论前一个层次的人捣乱。</div>

							
							
							
							
