<br />
&nbsp; &nbsp; * 表达自由<br />
&nbsp; &nbsp; * 信仰自由<br />
&nbsp; &nbsp; * 免于匮乏的自由<br />
&nbsp; &nbsp; * 免于恐惧的自由<br />
