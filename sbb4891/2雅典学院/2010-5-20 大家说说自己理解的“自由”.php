绝对的自由在理论上可能存在，但由于社会是由人与人所组成，自由不仅是个人的议题，而是社会中各个主体之间彼此互相界定的程度，因此有人认为[谁？]个人的自由受制于他人的同等的自由，进而有人认为[谁？]自由与责任相关，有相关之自由即应负相关之责任。<br />
