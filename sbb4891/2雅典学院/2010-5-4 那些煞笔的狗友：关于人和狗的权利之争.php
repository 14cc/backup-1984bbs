																											<div id="postmessage_597355" class="t_msgfont">相册所在地，颠鸾倒凤的“动物权利主义者”表演“良心”而对人类的仇恨的所在地：<a href="http://www.douban.com/photos/album/26634362/" target="_blank">http://www.douban.com/photos/album/26634362/</a><br />
<br />
 <br />
<br />
 <br />
<br />
狗友们无处不在。养狗爱狗是很正常的事情，可是有些家伙养了两条狗之后（或许有些甚至没养过狗），却变得爱心爆棚，对动物权利关怀备至到了极点。<br />
<br />
今天我在豆瓣上看到一个相册，相册的内容是关于被用于“医学实验用狗”。照片本身没有什么问题，可是照片后面跟着的那些评论就实在让人倒了胃口。<br />
<br />
一个一个爱心爆棚的、无比热衷于动物权利的狗友们满眼眶热泪地喊着“人兽平等”，喊着动物无辜，喊着喊着，后来居然有人提议说为什么不用犯人的来做实验？我xxoo你啊，犯人是人，狗是兽，你人的权利都不尊重，还满嘴巴的狗权，你是69式玩多了吧，连脑袋都颠鸾倒凤起来了。<br />
<br />
我也不知道这帮子小子在自己的同类的权利被赤裸裸侵犯的时候在哪里，倒是所谓的“狗权”被侵犯时却突然冒出来，这帮满脑子“平等”的家伙或许会这样说：“你连狗的权利都不尊重，又怎么能指望你尊重人的权利呢？”这种屁话实在听得太多了。同类的权利都没有保护好你就急着去保护狗的权利，你蛋疼吧，你是皇宫里面那娇生惯养小公主吧，为了疼爱你的那可爱的小宠物，不惜让身为你同类的奴仆们在地上打滚摔跤磕出一脑袋血来讨好你的小狗狗吧，为了不让你热爱的那几条狗不被用于医学实验，你不惜喊出拿犯人来代替狗这种屁话。<br />
<br />
还有点人说起侵华日军，说起731部队和南京大屠杀。这帮逼满脑袋奇怪的“平等”的家伙觉得“这和南京大屠杀有什么不同”“这和731部队干的有什么不同”。日本人中国人都是人类，人类交合能生下后代，所以保护人的权利是所有人都应该做的事情，而人和狗不能生下后代，人和狗并非同一个物种，这就是区别。为了自身物种的权利而适当地去牺牲一下别的物种的行为，这是所有生物都会做的。而这些“平等逼”居然连轻重都不分，甚至有些还认为拿狗去做医学实验是比南京大屠杀这种暴行更残暴的行为，我看这不能叫做“平等”，这应该叫做反向不平等，一句话概括之就是一些矫情蛋疼的“小公主”们溺爱小狗多于热爱人类权利的煞笔表现。<br />
<br />
这些矫情蛋疼的“小公主”们不知道是不是童话故事看多了，觉得大自然都是和谐友爱的，不存在生存竞争，不存在一种生物为了自身利益而伤害另一种生物，所以看到人类为了拯救同类的生命而拿犬只去做医学实验，登时一股“先进的西方左派政治正确激情”冒了上来，他们吵闹着说这是人类的罪行，这是只有人类才会犯下的罪行，这是人类与和谐友爱的大自然最大的区别（人类不是大自然的一部分？人类之所以能延续到今天不是因为在大自然那平等规则下竞争而得到的？），或许这些逼认为所有动物都是吃素的。<br />
<br />
人兽不分，轻重不分，视为煞笔也。<br />
<br />
有些煞笔又从这医学实验用狗上联想出去，想到了人类的罪行和天罚，联想到了2012，联想到了世界末日。<br />
<br />
最后，我拿叶子风的一段话来评论这帮逼：<br />
<br />
在醉眼朦胧的人生里，他看不到光亮，一直积攒着对人类的怨恨。为此他渴望世界末日来临，在人们惊慌失措的时刻，他来亲自演示良心的拯救力量。他先施加恐吓，再给予希望，继而唤醒昏睡的心灵。……拒绝相信理性的力量，一味追求神秘主义的体验，他们拥有的全部，就是那一点点可怜的良心。<br />
