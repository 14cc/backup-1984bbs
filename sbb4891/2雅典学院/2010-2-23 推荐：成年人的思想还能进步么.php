													<h2>推荐：成年人的思想还能进步么</h2>
						
						
																											<div id="postmessage_489206" class="t_msgfont">成年人的思想还能进步么？<br />
从 学而时嘻之 作者：同人于野<br />
有 18 人喜欢此条目<br />
<br />
本文谈谈成年人思想的进步。<br />
　　<br />
　　在我们上小学上中学的时候，不管学校的教育多么糟糕，我们的思想都在不停的进步。这表现在如果我们回首一年以前的自己的某些想法，做的某些事，会觉得那时候自己很傻。考察自己的进步的一个更客观的指标大概是“刮目相看指数”。别人如果一年没看到我们，再看的时候，第一反应往往是“你都长这么大了”。如果三年没看到再看，可能都不认识了。<br />
　　<br />
　　但是成年以后，一个很可能的趋势是人的进步速度一般会越来越慢，以至于停止进步，甚至倒退。他们思想最牛B的时代是他们的年轻时代。中年以后他们就无法接受任何新的思想，以至于最后连自己的孩子都搞不定。<br />
　　<br />
　　比如说歌手郑智化在过去十多年内就没有任何进步。我上中学的时候很喜欢郑智化，他的歌曲对我来说不仅仅是娱乐，而简直是一种教育。这么多年来我的思想进步了许多，可是最近郑智化在北京开演唱会，唱的还是当年那些老歌。伟大的郑智化，他为什么没有像翁美玲等美女那样在人生最伟大的年龄上死去，何必让人间见白头？<br />
　　<br />
　　余秋雨的思想在过去这么多年内很可能还退步了。我最初知道他是上初中时看《读者文摘》上他的文章，高中以后我就再也不看《读者》，可是他现在连那种文章都写不出来。<br />
　　<br />
　　最有意思的一点是，并非所有人都会在中年停止进步。比如赵本山就一直都在不停的进步：<br />
　　<br />
　　- 最初赵本山是一个二人转演员。对于绝大多数二人转演员来说，上一次电视可能就是他们人生的顶峰；<br />
　　- 然后赵本山变成了一个小品演员。对于黄宏这样的小品演员来说，走红全国就是他们人生的顶峰；<br />
　　- 但赵本山继续进步，演了很多电视剧和电影，而且收视成绩相当好。对于冯巩这样的演员来说，又能上舞台又能演电视剧又能演电影就是他能力的极限；<br />
　　- 但赵本山仍然在进步，他不但演电视剧，而且自己当导演，而且很成功；能演能导，这是一个“艺术家”的极限么？<br />
　　- 但赵本山证明他不仅仅是个艺术家，他还是个实业家：他办影视基地等“产业”，他甚至试图买下一只足球队拯救中国足球；对郝海东来说，既能当好运动员有能当好商人就实现他的人生蓝图；<br />
　　- 就在我猜想赵本山还能怎么进步的时候，他又推出了一大帮学生，证明自己甚至还是个好老师。<br />
　　<br />
　　作为一个东北人，我热爱赵本山老师的所有节目，并且鄙视那些说这些节目粗俗的人。但我最佩服他的一点是他能够不停的进步。可能很多人会讽刺说他只不过是个爱折腾的贪婪的人，可是别人怎么就做不到呢？今天的赵本山一定可以让十年以前的他刮目相看。赵本山的例子显然说明年龄等生理因素不见得是阻碍我们思想进步的原因。<br />
　　<br />
　　从事脑力劳动的人通常都能够在中年以后继续进步。比如王朔就能在成名之后还进步。可能很多人不喜欢王朔的进步，但他进步了。然而“脑力劳动”这个标准似乎也不准确，比如金庸过去几十年内就可以说没有任何进步，就连剑桥大学也无法使他进步。<br />
　　<br />
　　我认为阻碍人进步的因素有两个。<br />
　　<br />
　　第一个是早期的辉煌会把人的思想留住。有些人曾经在比如说大学时代是一个很优秀的学生，进入社会之后一旦受到挫折，他的思想就会永远用来回忆大学时光。梁启超说，“老年人常思既往，少年人常思将来”。一个人一旦开始回忆，他基本上就会停止进步。<br />
　　<br />
　　第二个因素更普遍，需要详细说说。心理学认为人对于外部世界的认识可以分为三个区域：舒适区（comfort zone） ，学习区（learning zone），和恐慌区（panic zone）。<br />
　　<br />
　　比如说我们看一本书，如果这本书所说的内容都是我们所熟悉的，完全符合我们已有的世界观和人生观，这本书就在我们的舒适区内。<br />
　　<br />
　　但如果这本书说的内容与我们原有的世界观和人生观不符，但是我们思考之后仍然能够理解接受，那么这本书就在我们的学习区内。<br />
　　<br />
　　如果这本书我们根本就理解不，那么就是在恐慌区。<br />
　　<br />
　　心理学研究说，只有在“学习区”内做事，人才会进步。绝大多数人工作以后干的事情都是他的舒适区内的某种意义上的重复劳动，这怎么能进步呢？<br />
　　<br />
　　金庸小说和各种消遣读物都在舒适区。一个很有意思的现象是很多三十多岁的人仍然一天到晚谈金庸。其实这些东西都应该在大学毕业以前看完就没必要再提。三十以后还整天谈金庸的人，可能四十岁以后唯一的谈资就是他们的孩子。<br />
　　<br />
　　现代社会分工的一个特点是尽量把人往他自己的舒适区里分。你想干有挑战的工作，你老板却只想给你你胜任的工作。所有人都想永远挑战自我，但实际生活中人们主要的时间都在做一些驾轻就熟的事情。<br />
　　<br />
　　随着年龄的增长，一个人获得挑战自我的机会会越来越少。这往往并不是年龄歧视，而更多的是市场合理调配的必然结果。只有那些有特殊机遇的人才能不断尝试新的生活。<br />
　　<br />
　　所以我们又能怎么办？办法无非就是尽量呆在自己的“学习区”里，并且随时对“舒适区”保持警觉。<br />
　　<br />
　　可能很多人会说赵本山活的那么累有什么可值得学习的？但我说的进步是思想上的。一个人的进步不见得非得从演员变商人。那些最牛B 的科学家一生都是科学家，但他们一生都在不断的进步。他们的世界观和人生观不是定死的，而是活的，不停地演化。<br />
　　<br />
　　相比之下，倒是有很多年轻人，他们的脑浆已经从液体变成固体，只接受符合他们原有观念的东西。你跟他们说你落伍了，他们会说他们的世界观人生观已经成熟地形成。这可是连老头子都不敢说的话啊。</div>

							
							
							
							
