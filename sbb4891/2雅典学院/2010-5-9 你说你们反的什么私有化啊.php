													<h2>你说你们反的什么私有化啊</h2>
						
						
																											<div id="postmessage_606014" class="t_msgfont">没有私有产权就没有自由。私有制是自由、宪政和民主的基石。要想获得自由、宪政和民主，就必须首先实现私有制。<br />
<br />
然而，中国的私有化运动遭到部分民众强烈反对，现在已经半途而废（也有人认为已经私有化了，到了官员的腰包了。这一说法另论）。反对者认为，私有化过程就是现有掌权者受益最大。由此，他们愤怒地质问：凭什么？我说，凭的就是他们是掌权者。这一说法一定激怒你了吧？！别急，回顾一下奠定欧洲私有制基础的圈地运动，相信你的气会消掉许多。<br />
<br />
封建时期的欧洲，土地均归国王所有，贵族和封建主的土地属于国王封赏，可以世袭，也可以被国王收回。圈地运动开始后，贵族和封建主的土地及一些公地变成纯粹个人的私有财产（君主制下所有财富包括土地至今名义上依然归国王所有，但仅仅是名义，没有实际意义，也有少量真正的公地）。圈地过程中，贵族和封建主原来的土地自然被他们永久占有，其他一些公地有被贵族和封建主占有的，也有被此前完全没有土地的人占有。如果说这后一类占有还有凭借体力这一天然优势而显得公平一些的话，对于贵族封建主永久占有的土地，人们当然也可以问一句“凭什么？”。显然，凭的就是特权！<br />
<br />
圈地运动也遭到一些人反对，尤其在历史学家和文学家笔下，那场运动被部分地描绘成一场弱肉强食的“丛林游戏”（其中有许多不实之词，实际上并没有那么血腥）。然而，正是圈地运动奠定了欧洲的私有制，既提高了农业生产率，又培育了一个新兴的有产者阶层。这个阶层在此后的经济、社会和政治生活中，不断地向王权施加压力发起挑战，通过参与规则制定等方式，最终为自己也为穷人争得了自由，实现了宪政民主。<br />
<br />
圈地运动中贵族封建主阶层显然凭借原有特权占到了便宜，其他那些体力优胜者也一样。试想，如果当初大家都执拗于“凭什么”的怒气而致那场运动夭折，那么，欧洲有没有此后的辉煌历史就难说了。<br />
<br />
当然，几百年前的文明程度不能与现在相比，如何将走入公有制歧途的国家重新拉回私有制轨道，当然不能像圈地运动那样“野蛮”，但，相同的一点是，转型必然要有代价，它是无法完全避免的，必须承认和承担。圈地运动时的代价（无特权者所失）就是贵族封建主凭借特权占了地，现在的公转私固然不会太过明目张胆地让特权阶层占便宜，但实际上特权阶层一定会利用权力上的方便多吃多占。圈地运动是特权阶层从国王手中占了便宜，现在的私有化就是特权阶层从国家（或全社会）手中占了便宜。<br />
<br />
私有化中特权阶层占便宜当然很扯淡，但那实在是无法完全避免的。如果民众因为“气不过”而拒绝私有化，那它就是从私有化“上当”中醒悟过来的当局所求之不得的，随之而来的国进民退再明显不过了。小不忍则乱大谋。正如人们看到的那样，私有化一夭折，民众原本已经得到或即将得到的权利也被或即将被“收归国有”，由此，专制政权得到进一步巩固。<br />
<br />
反私有化的有三种人，1，毛左，2，当局，3，相当一些普通民众。本文是说给第三种人听的。你说你们反的什么私有化啊，与“收归国有”造成的多数权利丧失相比，特权阶层多吃多占那点代价算得了什么啊！<br />
