													<h2>是支持温家宝 停止讽刺他的时候了</h2>
						
						
																											<div id="postmessage_601892" class="t_msgfont">温家宝的天安门广场在哪里？<br />
(博讯北京时间2010年5月07日 首发 - 支持此文作者/记者)<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp; 由于博讯最近报道了总理温家宝的几条消息，引起读者不小的反响。日前还报道了温家宝总理不参加世博开幕式，而独自一人前往玉树过五一，几乎一下子抢走了在上海参加世博会的七位常委的风头。这则新闻被多家香港媒体掐头去尾地引用，引起广泛关注。大多读者在来信中想知道更多有关温家宝最近活动动态的情况。为此，博讯记者接通目前正在上海出差的云飞扬，就一些读者的问题询问他的看法。<br />
&nbsp; &nbsp;&nbsp;&nbsp;(博讯 boxun.com)<br />
<br />
&nbsp; &nbsp; 博讯记者：有读者问，自从温家宝两会说自己时间不多了，要利用这个时间为人民多做事，感觉他确实越来越“出格”，请问他是否真会有什么大动作？<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 云飞扬：温家宝堪称体制内的异数，但也毕竟是体制内的，要想打破目前的体制而有所动作，以温家宝从来不搞拉帮结派，至今几乎在党内还是孤家寡人来看，他很难有所突破。但话说回来，作为一名总理，他目前的一些言行，已经可以算是“大动作”了。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 博讯记者：这就是问题所在，我们也注意到，每次当温家宝说出很出格的言论，例如公开挺普世价值的时候，网络上都会出现一片嘲讽之声，说他是“影帝”，光说不练。同时，肯定就有人放出小道消息，他的家族如何如何贪污腐败了。你怎么看这个问题？<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 云飞扬：这是个大问题。温家宝的行动确实和他的言行有相当大的差距，但考虑到他目前在中央的地位，以及整个国内的形式，他能怎么样。可是，作为一位总理，他能够利用自己的言论平台，经常说一些对大众有启发意义的话，难道本身不就是一种大的行动？这个国家最差的就是言论自由，总理带头“言论自由”一下，也是值得鼓励的。如果出现体制内外良性互动，那就善莫大焉。对温总理的评价，怎么多元都不为过。但我也注意到一个现象，感到很不安。只要温总理一讲一些好听的话，包括普世价值，跳起来攻击他的竟然包括一些国内外高举民主大旗的知识分子，这就很让人惊讶了。中共高官到处都在闷声发大财，好不容易出来一个温家宝，你不借他的话做文章，发扬光大也就算了，为何要用那种刻薄的语言对付一个党内思想比较开明的人？<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 博讯记者：是的，说他作秀的网民大多是有点名声的。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 云飞扬：那些嘲讽温家宝“作秀”的人，首先把自己看得太高，温家宝有必要给你作秀吗？七千万中共党员，有几个人给你“作秀”？再说，你们一些知识分子，不也是每天在网络上推广普世价值，也没有看到你们到天安门广场游行示威，是不是也是作秀呢？当然，我不是说不能批评，故意刺激一下温家宝也好，但感觉有些知识分子太过了，他们甚至专门找温家宝的茬，无意之中几乎完全占到另外一派那一边，亲痛仇快。有时，我甚至怀疑他们真是那一派的。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 博讯记者：你说的那一派是指谁？中共中从来不作秀的顽固分子？<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 云飞扬：我说的另一派是有目共睹的，在北京也是公开的秘密。你们记者也应该注意到，凡是温家宝讲话过头，或者想有所动作的时候，网络上马上就及时公布他儿子和老婆贪污腐败的事。其实，这事首先并不是贪污腐败的范畴，按照目前中共的普遍贪污腐败来说，他老婆和孩子的生意还算是没有违法，这些我们都有关注。其次，温家宝和儿子老婆的生意还是保持了一定距离的。温家宝早就给自己手下打过招呼，他们做生意和我无关。可是，只要温家宝说话一过头，马上就有这种消息放出来，其实，我国安部另外一个部门的同事早就知道这些消息的来源，也都收集了一些资料私下留了下来，时候到了的时候，也会公布出来的。这些人是谁？正好就是贪污腐败的利益集团，包括现在两个政治局常委的亲属都直接卷入了散布温家宝家族腐败的谣言，当然具体的情况也只有他们知道。他们为什么要这样，就是要提醒你温家宝，大家都在一条船上。你别想把船弄沉。另外就是，他们不但不喜欢温家宝“作秀”，而且很有点害怕，感觉温家宝随时会假戏真做。这就是我说的那一派。问题在于，那一派发出消息后，传播这些消息的反而是当今在海内外比较活跃的一小群知识分子和维权人士，他们主张改革，怎么竟然去帮那些反对改革的？反对贪污腐败没有问题，全中国所有的党委书记都在贪污腐败，可他们偏偏要挑选温家宝。不要他“作秀”，好在那些镇压他们的都从来不屑于作秀的。你去看看中国维权和知识分子比较集中的twitter,你做一个简单的统计就会发现，讽刺挖苦批评温家宝的推竟然比其他所有政治局常委加起来还多。其实，我有时确实有些怀疑，这些人中大概也混进了一些腐败利益集团的卧底。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 博讯记者：这个现象说明什么？<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 云飞扬：这个现象说明什么我不好下结论，但至少说明中国远远没有出现上下良性互动的局面。温家宝这种人真要有所作为，在目前的情况下，只有民意可以扶持他，因为他几乎是无权无势的。可是，如果你是温家宝，如果你说了一通赞扬普世价值的话，看到那些整天推崇普世价值的知识分子把你当成敌人，你还有力量和胆量走下去？这反而正是中央那帮嫌温家宝“作秀”的腐败分子愿意看到的结局。但一次又一次借反腐败的维权和民主人士做到了。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 博讯记者：知识分子就是以批评为己任，批评温家宝应该没有什么问题吧？<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 云飞扬：我不知道知识分子到底应该怎么做，但我同意你的说法。我不是说不能批评，而是我觉得他们挑选温家宝，也许找错了目标。正如21年前，赵紫阳作为总书记，当然应该是学生们批评和强硬的对象，当时赵紫阳的儿子经商被揭露出来――后来如何？后来被一一证实，所谓赵紫阳儿子经商，所做的生意还不到一百万人民币，而透露出这个消息的正是那些想置赵紫阳于死地的，包括陈云与李鹏的部下。当然，传播这些消息的恰恰是赵紫阳想保护的激进学生。当时一些强硬派甚至要借学生攻击赵紫阳儿子经商的事，建议先把赵的儿子控制起来，以要挟赵紫阳。如果我们稍微有头脑，或者别以为真理在手，所向无敌，稍微讲一点策略的话，就应该知道，在那种情况下，赵紫阳应该是团结的对象。如果当初真有一个良性的上下互动，情况可能会不一样。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 博讯记者：可温家宝不是赵紫阳，你把这两个人相比较，不太妥当吧？<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 云飞扬：你怎么知道温家宝不是赵紫阳？如果没有天安门事件，你又知道赵紫阳是什么样的？别神话赵紫阳，他在位前后也近十年，也发表了不少讲话，做了好多年总理和总书记，你比较一下就知道。是天安门广场把赵紫阳推向了历史的高度。温家宝当今的天安门广场在哪里？就在网络上，温家宝利用网络突出理念，你们也看到了，他很多话除了网络上可以流传出来外，中共的各大媒体根本不刊发。要说网络维权，温家宝就是做的比较好的，作为总理，党控制的报纸不发表他的言论，他就不停地利用和大家交流的机会在网络上“发布新闻”，希望有所突破。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 博讯记者：但总感觉到还是没有大的突破，而且在近期内也看不到希望。和那些批评温家宝的人相比，你是不是太把希望寄托在温家宝身上？<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 云飞扬：和你说的恰恰相反。我感觉到那些动不动就猛批温家宝的人反而是因为把希望寄托在一个人身上而感到失望甚至绝望所致。我们说应该和温家宝党内的稍微开明一些的人建立良性互动，不是说把希望寄托在他们身上，而是寄托在我们自己身上。别忘记了我前面所说的，赵紫阳是天安门的产物，如果没有学生们的天安门，有赵紫阳的天安门吗？同样的道理，那些整天期盼温家宝拍案而起的人，你自己做了些什么？如果你自己整天在那里连“作秀”都不去做，指望温家宝做什么？<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 博讯记者：今天正巧，就在采访你之前，看到国内著名作家杨恒均博士一篇写戈尔巴乔夫的文章，里面有这样一段话：“戈尔巴乔夫不是没有碰上困难，例如每当他言论出格的时候，总有一些人攻击他‘作秀’，而每当他提出新的改革措施，又总有人到处散布他妻子赖莎如何招摇、家族如何腐败的消息，而且，他在很多公开场合宣扬一些人道与自由价值的讲话，到了《真理报》发表时，总会掐头去尾，甚至压下全文不发。不过，人心思变，是党内强大的要求变革的力量，以及党外更加强大的渴望改变的民意，始终支撑着戈尔巴乔夫的改革。”看起来，受到攻击的不只是温家宝，还有戈尔巴乔夫。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 云飞扬：这两个人不好一起比，相差还很远。我想强调的是各方面都要努力，但要有一定的策略，也要有一定的宽容，特别是那些和我们有些共同之处的人，不管他在体制内还是体制外，多一些沟通、理解和宽容，事情会好办多了。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 博讯记者：谢谢云大侠，希望能够经常听到你的意见。</div>

							
							
							
							
