<br />
作者：【安替 】 发帖时间：2003-03-22 20:05:00<br />
<br />
　　发言内容：<br />
<br />
　　当昨晚（21日）美军实施“震撼”行动实施对巴格达市中心的“精确轰炸”时，所有卫星台都保持了一分多钟的沉默，最难以忘怀的就是英国ITN的画面，随着一声轰响，一个小蘑菇云升了起来。拉姆斯菲尔德说，今天你们从电视上看到的武器的精确性，是你们从来没有见过的。不但如此，连在巴格达的电视记者都被炸弹的威力吓坏了，只是后来一个劲地说，我从来从来没有看到过如此巨大的爆炸。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 如果有什么话能形容我的心情的话，那就是“我的心被彻底炸碎了”。在爆炸点周围，就是政府雇员的住宅区，那天伊拉克司机还仔细告诉我这片住宅区的情况。这是政府奖励给公务员的住房，有中央空调，房租也很便宜。但司机告诉我，这里很危险，因为附近有很多政府机构，而且一两公里处还有萨达姆的新总统府。没想到，美军炸的就是这片地区，爆炸这么大的当量，说没有平民伤亡，很难以致信。 愤怒从心里升起。这根本不是一场战争，这是美国通过媒体向全世界宣布新罗马帝国的诞生，以如此傲慢无理的方式。地面部队在慢慢地推进，对方将领们却基本上都炸没了，全程还有电视直播。这算什么战争？这是一场TV秀！美国就是要秀给全世界看看，它是怎么占领一个曾经不受它管辖的地方的。从此，天下莫非王土，各诸侯以听话程度定亲疏。难道美国不知道这次战争让它永远失去了中东国家对他的信任吗？连亲美的约旦国王昨夜也紧急发表讲话，对全体约旦国民说，“我和你们一样的愤怒”，以免国家失控。难道美国不知道这样只能使更多的基地组织在中东产生吗？如果不是这些亲美中东国家的专制政府们在控制民众，还不知道要有多少人要攻击美国机构呢。美国难道不知道这样会让表面上还发挥作用的联合国彻底成为一个垃圾吗？法国已经发誓，要否决任何英美今后关于伊拉克的决议。美国当然知道，但是，经过昨天的“A-Day”轰炸之后，美国已经成为一个真正的帝国了。既然是帝国，就根本不在乎一切一切。什么不信任、什么国际社会分裂、什么仇恨，So What，美国根本不在乎。你不信任你的，你仇恨你的，反正帝国就是要控制一切的，你们又能拿我们怎么样。911事件之后，布什政府下了决心，不允许任何地方成为美国控制的盲点。欧洲精明的领导人，如英国首相、西班牙总理、土耳其总理，都非常清楚目前的帝国局面，所以尽力说服自己的民众，服从现在的新帝国统治，获得最大利益。反而是向来没有政治智慧的法国，还以为美国还是克林顿时期的美国，美国还有底线，会顾忌一些东西，所以做了反对美国的赌注，却一错再错。但世界上不仅只有欧洲，中东也不仅仅只是以色列、科威特，亚洲也不仅仅是日本和韩国，还有太多太多不说英语的国家不会也不会愿意服从新帝国统治。他们看到了什么？看到了所有以前的国际秩序被帝国规范重写。半岛电视台昨晚用一个镜头做节目的开始片头：美军占领第一个伊拉克小城市之后，用美国国旗换上了伊拉克国旗。对于一个中国记者来说，美国人这个行为略显浮夸：伊拉克人民非常随和没有抵抗情绪、伊拉克军队也早就不是美国的对手，何必用这么夸张的动作表达自己的“壮烈”呢？而对于阿拉伯世界来说，这个镜头就是难以磨灭的耻辱：美国人的国旗插在了阿拉伯的领土上。这个镜头会在很多很多人的心中沉淀下来，转化成仇恨，在适当的时候会向美国反击。不要忘了，拉登当年起兵造反的一个原因就是美国在沙特驻军，而埃及现在也批评伊拉克就是因为当初的侵略科威特开了美军驻军的先例。我问过很多阿拉伯人，他们怎么看这场战争。一些有教养的人都对我说了类似的意见：他们认为萨达姆的确很残暴，可是美国有什么资格这么打击伊拉克，有什么资格如此侵入伊拉克，给人以这么大的耻辱？如果这样，沙特的国王难道不也很专制吗？就一个普通穷人的感觉，难道在伊拉克巴格达生活不比在约旦安曼生活更有自信吗？如果说人权，那么联合国对伊拉克禁运12年造成的平民死亡难道不比萨达姆自己杀死的伊拉克人多吗？难道大规模杀伤性武器就值得让这么多人痛苦生活 12年吗？那么为什么印度、巴基斯坦很快就过关了？<br />
<br />
 《新闻周刊》最新一期特稿就是该刊主笔写的长文《傲慢的帝国――美国何以吓坏了全世界》也提出了相同的看法，它认为这场战争即使解决了伊拉克问题，但却加剧了美国问题：目前美国已经成为全世界人民害怕的东西，即便是支持，也是一种迫于压力的支持。反美成为全世界舆论的主流，那些不反美的领导人不得不考虑自己选票失去的危险，只有中东某些专制国家政府诸如沙特、也门，才可以利用专制继续控制民情、继续支持美国。这样的美国，的确让所有的国家和民众从心底发寒。下一次又是哪个国家的首都人民需要提醒吊胆美国的精确轰炸？曾经，美国是中国自由主义者最大的现实合法性源泉，但作为一个持自由主义立场的中国记者，我想说，伊拉克战争就是中国自由主义和美国分手告别的时候。<br />
<br />
现在的美国，希望的不再是克林顿时期的推行自由民主价值，而是希望让帝国的部队牢牢地控制全世界的天空、陆地、海洋，任何一处它不能控制的地方，都可以用“先发制人”的方式彻底制服。这样的美国，是我们必须保持距离的东西。自由民主已经是全世界的共识，再把自由民主和美国捆绑、支持美国的所有行动，将是自由主义的灾难以及中国自由民主的灾难，民众会因为我们对美国的错误支持而对自由民主产生不信任。因为此时的美国，已经不再美丽，而是一个彻头彻尾傲慢的新罗马帝国。</div>

							
							
							
							
