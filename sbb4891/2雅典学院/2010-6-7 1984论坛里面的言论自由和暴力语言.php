													<h2>1984论坛里面的言论自由和暴力语言</h2>
						
						
																											<div id="postmessage_661562" class="t_msgfont">1984是给人言论自由的地方，而且这个言论自由可能会突破一些文明社会的底线，比如爆粗口。所以有道德洁癖的坛友可以选择离开1984。<br />
<br />
一些文明底线的界定本来就是模糊的，1984对这个底线的把握是公平的，这点就足够了。对这个底线的把握是1984站长和管理者的权利，其他人无权。<br />
<br />
严格意义上讲，1984没有什么底线的。这是我在1984里面混的感觉。我自己也基本认可这条底线，这里有几个原因：首先，1984是个虚拟论坛，不会造成实质伤害；第二，降低标准可以看到更多的思想碰撞，只要你能够从一些秽物中剥离出来他的真实含义。<br />
<br />
这个对于有道德洁癖的人比较痛苦，其实我也是有这个洁癖的。但是凡事无完美。<br />
<br />
1984没有人因为爆粗口被处罚的。这是我观察的结果。只要管理者对粗口等等纵容的标准是一致的，就是可以接受的。这就是程序正义。程序正义远远比实质正义更重要。这是中国人不好理解的地方。纵容是管理者的权限，没有必要挑战。能够挑战的程序正义与否。<br />
<br />
至于程序正义和实质正义，这几乎是所有政治哲学的出发点。只不过很多时候大家意识不到他们的存在。简单地讲，程序正义就是“法治”，法最大，也许这个法不完美。而实质正义，要不就是哲人王，要不就是大民主。实现实质正义最好的路径是程序正义的日拱一卒，而摧毁实质正义的有效武器是以实质正义的名义废弃程序正义。<br />
<br />
<br />
所以，我认为，1984的管理者们基本上做到了程序正义，这是1984能够吸引大多数网友的原因。基于现在的大小环境和论坛特点，1984放低了实质正义的要求，这点有部分网友并不习惯。程序正义可以保障自由，而实质正义就很难说了。由此可以推出，1984充分保障了大家的言论自由，而且以这个追求为目标。1984降低了对语言文明的要求，这是1984管理者的权利，我看到攻击1984管理层的主要依据都与此有关，说明这种攻击不是在追求言论自由，而是越俎代庖，侵害了管理者的权益。<br />
