二、希望中国人不要光自夸，而是要去接近那些传统美德<br />
三、最大de担忧、最好玩的事情：现在的中国是完全两套话语和思想系统<br />
2009年12月23日，刚刚参加完比赛的韩寒就“2020，中国新十年”主题，接受了凤凰网与正义网的联合访谈，以下为书面访谈实录：<br />
<br />
　　规划在这个多变的世界里是没有用的<br />
<br />
　　凤凰网：对个人的下个十年，你规划过吗？你对自己下个十年的期待或者梦想是怎样的？<br />
<br />
　　韩寒：我只希望自己能够在下一个十年以后继续接到你们的问题，问我再下一个十年有什么打算，平安才是好。规划在这个多变的世界里是没有用的。<br />
<br />
　　凤凰网：你希望十年后，中国的赛车运动会发展成什么样？<br />
<br />
　　韩寒：如果十年以内中国的赛车不能抓紧时间向高端化发展的话，那么十年以后汽车运动就很差了。我只是好奇在很多个十年以后，也许内燃机要被电所取代，那个时候的赛车都没有声音怎么办。<br />
<br />
　　凤凰网：你对中国下十年的文学发展有什么期待吗？你期望今后的文学在社会里的角色和地位是什么？<br />
<br />
　　韩寒：这个是我最不关心的问题。因为文学在以前作为消遣的一种，地位很高，但是现在大家消遣的东西多了，资讯也多了，网络也解放了很多人的想象力和写作功夫，所以传统作家的东西可能会更臭更无味。我不看好文学。但文学将一直存在。<br />
<br />
　　凤凰网：在你的博客中，看到你对社会、国家大事都很关注，在下一个十年里，这方面的关注会不会更进一步或者有一些更实质性的行动？<br />
<br />
　　韩寒：我其实未必一定是个批评者，但我一定不是一个歌颂者。我没有什么实质行动的打算和计划，让老人自然老去就可以。<br />
<br />
　　希望中国人不要光自夸，而是要去接近那些传统美德<br />
<br />
　　凤凰网：你希望中国人在下个十年，出现哪些变化？<br />
<br />
　　韩寒：能够像我们经常自夸自己民族的拥有多少多少传统美德一样，去接近那些美德。我发现现阶段中国人的美德就是能说。<br />
<br />
　　凤凰网：具体到八零后、九零后的年轻人，你期待他们身上发生怎样的变化？期待他们在社会上能够拥有怎样的话语权、发挥怎样的作用？<br />
<br />
　　韩寒：还不是他们或者我们的时候，但是他们终将发挥他们的作用，比如很多年后，我不能想象一些80后的领导，站在台上，大谈什么精神文明建设之类的话。<br />
<br />
　　凤凰网：一些同龄人看完电视剧《蜗居》后，感慨爱情和婚姻被现实压力绑架，在下一个十年里，你觉得这种情况会发生变化吗？<br />
<br />
　　韩寒：基本不会变化，因为现在房地产成了支柱产业，是我过赚取给自己添面子的GDP的重要手段，而行政成本很大，很多地方政府只能靠卖地赚钱，房价高他们地价就高，对于那些能控制地价的人来说，只要他们有房子住，他们的儿女有房子住就行了。我其实希望房价一直上涨，物价一直上涨，上涨到二十万一平方米，油价二十块一升，工资还是像现在这样几千块，然后让全宇宙都来参观我们。<br />
<br />
　　最大的担忧、最好玩的事情是：现在的中国是完全两套的话语和思想系统<br />
<br />
　　凤凰网：对文化人或者知识分子在中国下个十年的角色与地位，你有什么样的期待？<br />
<br />
　　韩寒：期待他们在全宇宙都来参观我们的时候可以表现出他们应有的良知，给外星人留下一个好的印象。<br />
<br />
　　凤凰网：在你的梦想里，十年后的中国，是什么样子？<br />
<br />
　　韩寒：我的梦想里，只要政府有胆识给出政令，所有楼盘新开盘的价格不得高于上一期的开盘价格，那十年后的中国我想还是能住人的。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;凤凰网：对当下的中国、中国人，你最大的担忧是什么？十年后你希望能有什么样的改变？<br />
<br />
　　韩寒：最大的担忧当然也是最好玩的事情就是现在的中国是完全两套的话语和思想系统，我去过一些城市或者县城，发现那里的领导们其实完全活在另外一个空间里。大家在说各自的，玩各自的，对于这一批年轻人来说，除了玩的姑娘和这些领导们玩的是差不多一样的岁数以外，其他的都已经没有交际。现在只是井水不犯河水而已，十年后我不知道会有什么好玩的事情出现。</div>

							
							
							
							
