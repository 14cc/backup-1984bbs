													<h2>中国人该如何选择――写给“民主自由派”的一点看法</h2>
						
						
																											<div id="postmessage_620438" class="t_msgfont">昨天是香港516五区公投，虽然民主派受到明显压制，以特首曾荫权为首的、非普选的港府官员悉数拒绝投票，最终的投票率很可能比较低迷，但仍然有几十万人拿起了手中的选票，走向投票站，为香港的未来，投下了一票。<br />
<br />
&nbsp; &nbsp; 在推特上，很多大陆人为香港同胞摇旗呐喊，台湾同胞也在声援香港同胞，这种场面着实让人感动。<br />
<br />
&nbsp; &nbsp; 网上有句名言：“如果有些事你想不通，那么你想想你在中国，那么一切就想通了。”这句话很现实，也有些调侃的味道，但终究包含的是一种苦涩的无奈，在中国又如何？难道中国人的宿命就是在于坐稳了奴隶的位置？很显然，我们拒绝接受这个不合理的现实。<br />
<br />
&nbsp; &nbsp; 有人说，中国人奴性太强不适合民主制度，显而易见的是，台湾为什么可以实现三权分立，可以实现普选，可以是在主权在民？不要跟我提台湾的民主多么不健全，陈水扁总统也涉嫌贪腐入狱，我想说的是，陈水扁虽贪腐，但他受到了法律的制裁。任何一个体制都会出现问题，但看一个体制有没有希望不是看它有没有问题，而是看它除了问题之后是否有足够强的纠错能力。举个例子，四川地震中很多楼房倒塌并不是由于震级过高，而是由于豆腐渣工程，可是前不久，香港记者在北川暗访，居然又发现震后修建的房屋仍然是豆腐渣工程，这说明这些人根本就没有在地震中吸取教训，民众的生命在他们眼中根本不值得一提。而一个体制，出现问题没有即时的纠错，下场就是错上加错，错而又错，最终导致灭亡。而相比之下，台湾的体制虽然漏洞频出，但发现问题之后，能够及时纠正，举个例子来说，陈水扁公务机要费案东窗事发之后，台湾政府就收紧了公务机要费的控制，严格了公务机要费的管理，并有更多人来监督这笔款项的使用，很难想象有哪位总统再敢打这笔款项的主意。如此，即使这样的民主再有漏洞，这些漏洞也会被一一的堵住，导致这个系统完美的运转下去，这样的体制才是有活力的。即使是被称作民主体制的代表――美国政体也是如此发展过来的。<br />
<br />
&nbsp; &nbsp; 有人说集中制度高效，集中力量办大事。我笑了，主要是这个理由太可笑。你们说中国集中力量造了两弹一星，那人家美国不也造了吗？人家是民主体制不是应该低效吗？那人家美国怎么也造出来了？不光美国，英国、法国、就连印度不也搞出了具有印度特色的两弹一星吗？可反观中国，集中力量办了什么大事呢？远至大跃进、文革，近到三峡工程、世博会等等，哪样不是政府力排众议搞得？哪样不是弊大于利的？所谓集中制，只不过是一些人头脑一热，绑架所有国民跟着他们上一辆车驶向不归路而已。民主制政府是效率没有集中制的高，但成熟的民主制度国家里，每一项政策都是经过层层论证深思熟虑的，出台的政策也是稳健的，不可能是哪位领袖一时吃错了药导致全国人民跟着一起倒霉。<br />
<br />
&nbsp; &nbsp; 篇幅有限，我只挑这两个问题谈谈我的看法，有哪位如果想要与我辩论民主和集中请留言。<br />
<br />
&nbsp; &nbsp; 那么回到主题，中国人该如何选择。我想，民主制度在世界范围内是大势所趋，当然，我不希望政权一日崩溃，那将是灾难性的。我当然希望能够和平过渡，首先，某些人不应该只关注自己那点眼前的利益，应该以民族大义为出发点，国家未来为着力点，遇到问题要积极进行自我纠错，不要把民众逼得无路可走。现政权不是一无是处的，只要肯改变，一切都会改变，当然，这个改变时相当困难的，谁也不可能吧唾手可得的利益拱手相送，这条路还有很长，但希望一代人之内就能解决。<br />
<br />
&nbsp; &nbsp; 现阶段，最重要的不是搞煽动，不是搞革命，而是搞教化，你天天骂政府一点用也没有，太缺乏建设性，大陆人还没有做好准备迎接民主的曙光，一些完全没有民主意识，没有民主经验的国民是不可能搞好民主制度的，假如此时将美式民主制度塞给中国，搞普选、搞议会，那搞出来的肯定是暴民统治，国家也会四分五裂，陷入一片混乱，所以现阶段，我们的任务就是教化国民，搞一次“中国的启蒙运动”，今日香港的五区公投起到的教化作用远远高于实际意义。我们要拥有传教士的精神，积极向周围的人宣传民主思想，即使被白眼，被视为异类也不要放弃，因为我们要做的不光是为了我们自己，你们以后也会有子孙后代，你们还想让自己的孩子喝毒奶粉吗？还想让自己的孩子注射假疫苗吗？还想让自己的孩子住豆腐渣校舍吗？还想让自己的孩子被变态者伤害吗？如果你的回答是不，请你担负起民族的责任，请你用你自身的行动拯救这个国家。也许我们每个人的力量是小的，但合力却是巨大的。<br />
<br />
&nbsp; &nbsp; 但请记住，不要与政府为敌，我们需要的是渐变，而不是巨变，我们需要的是合作，不是对抗；不要煽动民族情绪，所有的民族都希望国家富强；不要挑起族群对立，我们要求同存异；不要挑起地域争端，不管你是南方还是北方，我们都是中国人。<br />
<br />
&nbsp; &nbsp; 微动力广天地，13亿个小小的力量也能成为巨大的动力。否则，当我们的孩子质问我们为什么国家会变成这样时，我们如何回答？我只希望我们能够推动中国改变，我们做不起民族的罪人。</div>

							
							
							
							
