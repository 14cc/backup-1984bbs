													<h2>新疆暴乱资料</h2>
						
						
																											<div id="postmessage_158746" class="t_msgfont">近代以来，新疆分裂势力发动的历次暴乱，都有针对平民的屠杀。<br />
新疆人民出版社1999年出版的《新疆反对民族分裂主义斗争史话》显示：<br />
1930年代“东突厥斯坦伊斯兰共和国”的暴乱中，“在莎车他（麻木提）又支持其心腹四团团副伯沙克尼亚孜捕杀团长沙衣木、县长阿合买提江等人，洗劫莎车县政府、税务局、银行库存的金钱财宝，以及人民群众的财物、马匹，县城汉人几乎被屠杀殆尽……”<br />
1940年代新疆三区革命期间，“在他（艾力汗 吐烈）的煽动下，一些地方发生了杀害战俘和汉族群众、污辱汉族妇女、抢劫汉族群众财物的事件。无辜汉族群众的生命、财产受到严重的伤害。”<br />
建国初至1980年代前，新疆分裂分子发动的暴乱，攻打党政机关，政府、公安局等目标，针对国家干部。《新疆反对民族分裂主义斗争史话》这本书里，没有显示大规模的平民伤亡。<br />
1981年，喀什暴乱。“那伙暴徒手持匕首、棍棒、皮鞭，呼喊着：‘打倒汉人’、‘打到黑大爷政府’、‘维吾尔斯坦共和国万岁’一类发动口号，横行于街市……在这次打砸抢事件中有六百多名无辜无辜汉族群众被打，其中两百多人致伤，2人死亡。”<br />
1990年代，分裂势力渗透到北疆。<br />
1997年，伊宁暴乱。“1995年以来，新疆跨地区的民族分裂主义‘东突厥斯坦伊斯兰真主党’头目排祖拉和一些重要骨干多次到伊犁活动，并派出二十多名南疆籍成员以讲经为名到伊犁进行所谓‘圣战’宣传，鼓吹民族分裂主义，发展成员和建立组织。……2月5日北京时间10时30分，数百名维吾尔族青年举着用阿拉伯文书写的两条横幅……非法游行，……12时许，约千余人集结在市人民医院门前……参加游行和闹事的人并不固定，此来彼去，时聚时散，其中夹杂着不少被裹胁蒙蔽的群众。为教育受蒙蔽者，在现场执勤的公安民警对非法游行队伍一再进行教育劝阻，但是队伍中的一伙暴徒非但不思悔改，反而以为我公安民警软弱可欺，……闹事的态势陡然升级，暴徒们手持棍棒、刀子、砖头、石块袭击在现场执勤的公安民警……2月6日，一小撮民族分裂分子……见汉族群众就打，遇汉族同志开的车就砸就烧，谁阻止他们的破坏活动就打谁、杀谁，杀人放火，无所顾忌。一伙暴徒在胜利路被服厂门前，用石头、棍棒把一位五十多岁的汉族人活活打死。在市人民医院十字路口，2名汉族人横遭暴徒毒打，其中一名被打得遍体鳞伤，气绝倒地，当场死亡，另一名被打成重伤，倒在血泊之中，奄奄一息……”<br />
伊宁“25”事件中，“198名无辜群众被打伤，其中50名重伤；7名汉族群众被残杀”。<br />
