7月7日，一队游行的汉族人曾经试图穿越警察的封锁线，到维族的聚居区。一个人对面前的警察喊道：“你不能保护我们，我们只能自己保护自己！”<br />
7月7日那一天，乌鲁木齐几乎所有的汉人都手持棍棒。一位记者拍到了这样的照片：医院的大夫、女护士拿着棒子在医院里走动；一个过马路的妇女，一手抱着婴儿，一手拿着棒子；还有一个年轻的女孩儿，一手拿着坤包，另一只手里掂着一个锈迹斑斑的铁锨头。这铁锨头或许是她随手从大街上捡来的，但她相信只有这件东西能给她带来安全。<br />
“只有开枪打死他们，才有震慑力！”这是同行的一位记者的观点。 <br />
作为一个人，人类中的一员，我认为这些杀人的人，已经不是人。这些畜生早就应该在犯罪现场被当场击毙！被当场粉碎！如果子弹打到了所谓不明真相的被蒙蔽的人，那么我要问：你为什么要跟这些畜生站在一起？你为什么要与这些畜生同行？<br />
政府屡屡惮于所谓的“友邦惊诧论”，所谓的“国际形象”。可我要问政府：你要对谁负责？你统治的合法性来自于所谓的国际形象吗？<br />
这些暴徒越过诸多政府机关的门口，越过交警大队的门口，越过派出所的门口，越过新疆维吾尔自治区边防公安大队的门口，将一家5口人砍头、肢解、焚烧。那10岁的小女孩儿不在家，幸存了。<br />
那么我要问：你怎么向失去所有亲人的10岁的女孩儿解释？这个孩子会怎么看待你？她会信任你吗？她将怎样的带着对你的质疑和怨恨度过一生？在未来的岁月里，除了被仇恨所折磨、吞噬以外，她又将怎样向自己的后代解释：那个夜晚，警察在哪儿？军队在哪儿？被她的父母亲按时按量缴纳的税金所供养的政府，在哪儿？？？<br />
在撕心裂肺的呼喊中，在亲人们哭红眼睛里，你的合法性在哪儿？你怎么对你的行为负责？你应该被起诉！<br />
鹿华坤的父亲，一个坚强的河南汉子，他的生活依然很困顿。采访结束，临走时，我掏出二百块钱，塞到他手里。我解释说：“作为记者，我采访完了，履行完了自己的职责。这钱是我一点心意，一定要收下。”这个汉子死活不要，硬是把钱推到我兜里。<br />
现在是你该深刻反思的时候了！血怎么可以白流？<br />
