																											<div id="postmessage_158750" class="t_msgfont">“高层没有预判到事件的严重性，这是其一；其二是事件发生后，没有能够采取有效的措施将暴徒驱离、控制。即使不开枪，用高压水笼头、用催泪瓦斯震慑并努力驱散他们，可惜这些都没有做。”<br />
