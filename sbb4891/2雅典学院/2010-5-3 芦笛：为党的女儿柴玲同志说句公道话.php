显，但后来巴黎肉食店供应的却是断头台源源提供的人肉。是群体圣徒还 <br />
是群体刽子手，那转化可以发生在旦夕之间。其原因毫不难明白：情绪从 <br />
来就是翻来复去毫无稳定可言的玩意，只有理性才是持续的。 <br />
<br />
总而言之，当个体自发形成集体时，唯一的驱动力就是情绪，理性说服毫 <br />
无作用，而煽动群众效果最强烈的情绪就是仇恨，一旦群众被仇恨刺激起 <br />
来，则立刻要出现互相攀比竞赛的恶性循环，最终使得大家彻底丧失神智， <br />
变成丧心病狂的野兽，什么灭绝人性的勾当都能脸不变色心不跳、毫不犹 <br />
豫地干出来。” <br />
<br />
然而这并不是原谅柴玲的理由，我早在旧作中反复说过了，因为毛共的“斗争哲学”教义将“妥协”当成最可耻的罪行谴责，而这套党文化教义就是指导八九学运的基本精神，使得学运成了毛共党文化的强大负筛选场，把所有温和的学运领袖都撵下台去，同时也为精通共党斗争权术的野心家们提供了一个排众而前、青云直上的良机，柴玲就是这种经过层层负筛选脱颖而出的人渣。 <br />
<br />
局外人不知道，当学运领袖其实具有强大的诱惑，镁光灯下全国瞩目的风头能极大地满足人的虚荣心，而呼风唤雨、一呼百应、改天换地、书写历史的虚幻的权势感与自我陶醉感简直就不是凡人能抵抗的。柴玲就是冲这爬上去的，她已经在那个臭名昭著的讲话中不小心泄露了天机： <br />
<br />
“那么多人争夺权力，我从来不迷恋权力，我只是为了，为了良心，我才不 <br />
愿把这个权力放弃给那一小撮那种投降派和阴谋家。但是我不知道这些人为 <br />
争夺这权力，而发起一次一次的攻势。我感觉这不对（哽咽）我就是想全中 <br />
国的中国人，全世界的华人，我就是想说中国人，我们作为这个民族太不幸 <br />
了，不要再自相残杀了，中国的机会已经不多了。（哭）” <br />
<br />
这段话也泄露了她采用的争夺权力的战略：抢占最高的道义制高点，显示最坚定的革命立场与毫不妥协的骨气。这就是扫荡一切竞争者的最强大武器，必能让她在这激烈竞争中胜出。除此之外，她也深通共党那套权术。据戴晴说，柴玲的城府之深与她的年龄极不相称，学领等人开会时她一言不发，下来后便在学生中散布流言蜚语，指责谁谁是阴谋家，投降派，要出卖民主运动。 <br />
<br />
戴晴的揭发我看完全是真实的，在1989年5月28日那个臭名昭著的讲话中，她从头到尾都在指责“阴谋家”、“投降派”、“特务”、“奸细”，吹嘘自己怎么怎么与这些人作斗争，像煞了江青同志的“两条路线斗争大事记”： <br />
<br />
“我想，种种迹象表明，这是一场有组织、有预谋的计划。种种迹象表明， <br />
而且我们已经得到一些消息，政府正在收买学生中的一些变节分子，主要 <br />
他们想大力地扭转和削减广场上的组织领导力量，整个葬送我们这场学运。” <br />
<br />
“对，对。更可怕的是，有些同学逐渐被政府收买，他们作一种学生中的 <br />
特务和奸细。这么跟他们讲，你们能让同学撤走的话，那你们就是人民的 <br />
功臣，国家的功臣，人民会记得你，国家会记得你。如有些死硬派不撤的 <br />
话，那么周围的便衣随时可以调动。” <br />
<br />
“可好多同学都不明白，我们现在在广场这是我们最后唯一的阵地了。有 <br />
人一再主张撤，这撤，唯一高兴的就是政府。我悲哀的是什么呢？我是总 <br />
指挥，我一再要求这个权力，掌握这个权力，就是为了抵制这种妥协，这 <br />
种投降派。而且作为北京高联和外高联，外校的高联，他们很愿意要这种 <br />
权力。” <br />
<br />
“作为一些知识阶层的人，成立了一个知识……什么各界联合会议，爱国 <br />
维宪委员会，在昨天会上我很愤……愤慨，因为我感觉到这些人也是在利 <br />
用学运重新塑造他们的形像。我一再抵制这种倾向，象刘晓波把吾尔开希 <br />
（断）对不起，上面说的可以删一下吗？要推举他作发言人什么的。我在 <br />
运动中对这类人有些看法，吾尔开希，就是他，曾经利用他的影响，他所 <br />
处的那种领袖地位，对整个学运产生了很大的破坏作用。这已经发生两次 <br />
了，最后一次给我们坚决抵制，而且罢免了。但现在他们有些人要重新树 <br />
他这个形像。” <br />
<br />
“尤其可悲的是，有一些同学，有一些什么上层人士，什么什么人物名流， <br />
他们居然为了达到个人的目的，完成自己的一些交易，拼命地在做这个工 <br />
作，就是帮助政府，或者不让政府采取这种措施，而在政府最终狗急跳墙 <br />
之前把我们瓦解掉，分化掉，让我们撤离广场。 <br />
<br />
“如果是这种同学们自我崩溃，自我瓦解这样一种情况下，我们要，要撤 <br />
回原地的话，那么中国就会这样的一种情况：党内的所有的比较先进的什 <br />
么思想有点民主意识的人，还有历次运动中，象什么四五运动，象什么反 <br />
自由化，清除精神污染历次运动中没有被打下去的人，这次一下全被清洗 <br />
乾净。” <br />
<br />
“我昨天非常痛心，因为我，（哭）因为我，又一次看到形形色色的人在 <br />
出卖这场运动，在葬送这场运动。” <br />
<br />
“我们本来有五六百人，现在只剩下十多个人。说我们这些人都坚持下去， <br />
都是坚定分子。” <br />
<br />
“那天我从知识界联合会，就是昨天，回来的时候，我哭了。（哭）我感 <br />
觉到我那么热爱广场上这几万万的孩子们。（哭）我有责任用我们的生命 <br />
坚持到底，但是我又觉得我很悲哀，我实在是无力回天，（哽咽）我一个 <br />
人太有限了，……我就是想说中国人，我们作为这个民族太不幸了，不要 <br />
再自相残杀了，中国的机会已经不多了。（哭） <br />
<br />
“二十七号撤离这个决议是所谓的爱国维宪各界会议决定的。他们造成了 <br />
很坏的影响，当时我也在，但是我没有很明确的意识到有这么大的危害力。 <br />
同时，兴奋点集中与他们有一些人要通过这个运动树一些自己的形像，什 <br />
么什么的，而且我没有坚决抵制这种东西。他们这些人决定在新闻招待会 <br />
上发布，来不及再更改了。但是后来我们一再声明，还造成很坏的影响。 <br />
但是可以说肯定有人坚持下去的，因为我就想告诉大家，现在广场是我们 <br />
唯一的阵地了，我们再失守这个阵地话，那么中国就要复辟了，我想每个 <br />
同学都有自己的判断。” <br />
<br />
这些话泄露了她“保卫天安门”的决心来源。六四发生前夕，学运已经处处显示了难以逆转的自然消亡颓势，根本不是早期那种轰轰烈烈风起云涌的局面了。学生们耐不住持久战与消耗战，广场上的人寥寥无几，以致学领们不能不在那儿树立个民主女神像招揽游客。柴玲的讲话流露出了这种“无可奈何花落去”的悲凉与绝望： “我们本来有五六百人，现在只剩下十多个人。”特别是“爱国维宪会各界会议”议决27日撤出广场，更激起了她的绝望的恐惧，理由她也如实讲出了： <br />
<br />
“当时我参加运动的时候，也就是25号那天，在4月25日那天在筹委会， <br />
当时可能混进了便衣或什么，我们在聊天，我说政治犯判多少年？他说以 <br />
前判三年，后来判五年，现在加到七年，后来加到17年。当时我很悲哀， <br />
我在想十七年以后我出来就四十岁了，很不甘心的呀。” <br />
<br />
很明显，如果学生真的遵照那决议在27日撤出广场，运动和平结束，则她就要被政府投入大牢，蹲上17年的大牢，她怎么也不能甘心，于是就决定死赖在天安门广场这个“唯一的阵地”上，这理由她也说得明明白白： <br />
<br />
“可好多同学都不明白，我们现在在广场这是我们最后唯一的阵地了。有 <br />
人一再主张撤，这撤，唯一高兴的就是政府。我悲哀的是什么呢？我是总 <br />
指挥，我一再要求这个权力，掌握这个权力，就是为了抵制这种妥协，这 <br />
种投降派。而且作为北京高联和外高联，外校的高联，他们很愿意要这种 <br />
权力。” <br />
<br />
“政府这方面已经逐渐的稳固了。我们唯一能做的就是我们在天安门广场 <br />
坚持，等待看一看人民能不能真正团结起来，因为到最后只有是人民跟这 <br />
个与人民作对的政府来较量了。 <br />
<br />
同学们老在问，我们下一步要干什么，我们能达到什么要求，我心里觉得 <br />
很悲哀，我没办法告诉他们，其实我们期待的就是，就是流血。就是让政 <br />
府最后，无赖至极的时候它用屠刀来对着它的，它的公民。我想，也只有 <br />
广场血流成河的时候，全中国的人才能真正擦亮眼睛。（哭）他们真正才 <br />
能团结起来。 <br />
<br />
但是这种话怎么能跟同学们说？尤其可悲的是，有一些同学，有一些什么 <br />
上层人士，什么什么人物名流，他们居然为了达到个人的目的，完成自己 <br />
的一些交易，拼命地在做这个工作，就是帮助政府，或者不让政府采取这 <br />
种措施，而在政府最终狗急跳墙之前把我们瓦解掉，分化掉，让我们撤离 <br />
广场。如果是这种同学们自我崩溃，自我瓦解这样一种情况下，我们要， <br />
要撤回原地的话，那么中国就会这样的一种情况：党内的所有的比较先进 <br />
的什么思想有点民主意识的人，还有历次运动中，象什么四五运动，象什 <br />
么反自由化，清除精神污染历次运动中没有被打下去的人，这次一下全被 <br />
清洗乾净。而且邓小平就说了，是有极少数的人，党内有，好像社会上也 <br />
有，学生中也有一小撮。” <br />
<br />
所以，真实的图景与陈大夫的想当然完全相反，不是学运推着柴玲辈向前，而是柴玲力挽狂澜于既倒，她靠最坚定的革命立场、高度的革命警惕性与路线斗争觉悟，打倒了形形色色的“阴谋家”、“投降派”与“特务”，坚决反对首都爱国维宪各界会议撤出广场的决议，率领十几个人赖在广场上，自任“保卫天安门广场总指挥”，甚至与副指挥李禄策划自焚，使得市民们为了保护他们不能不去堵入城的军车，成全了当局的大屠杀。如果不是柴玲等少数人靠抢占党文化斗争哲学的道义最高点，劫持了学运，学生早在六四到来前就撤退了，那也就不会有大屠杀的发生。当然柴玲辈不免被投入黑牢，但毕竟代价要小到不可胜计。 <br />
<br />
陈大夫和其他柴玲同情者的误区，是以党国统治者或民主国家的领袖去推想学领，又不曾如我这样，不但全程密切同步跟踪学运，过后还看了好几本西方专著，对学运相当熟悉，却以想当然代替真实图景，以为柴玲如常规领导那样，身不由己地被民众推着走。其实哪有这种事？八九学运是纯粹自发的群众运动，学领随时可能被群众抛弃，只有蓄意以最激进的口号蛊惑群众，并先发制人抓“特务”、“奸细”、“投降派”、“阴谋家”的野心家才能始终保持“权力”。 <br />
<br />
柴玲就是靠这策略爬上去的野心家，当运动转入自动消亡的低潮时，她害怕政府秋后算账，率领一小撮人赖在广场上，以情感讹诈（emotionalblackmail）的下作勾当打动市民的侠义心肠，让他们为保护学生们去与钢铁怪兽拼搏，最后一个个陈尸街头，换来的却是那伙人渣 “身有彩凤双飞翼，腰藏美元一路通”，由中央情报局的保镖护送出国（这是吾尔开希到达西方首次露面时西方报纸的报道）。如果不谴责这种以民命为刍狗的下流行径，中国就永远断不了此类悲剧。宽容这种毛共“运动群众”的下流战略，不仅对不起长街上倒下的许许多多生灵们，也无异于对未来的中国人犯罪。 <br />
<br />
最后说点无关的个人观察。个人觉得，陈大夫其实是最没有宽容精神的同志，不但见不得同性恋，而且连天主教、东正教、新教的路德教、安立甘宗都无法容忍，体现了典型的中世纪的基督教特色（如今鄙人所在的地区已经实行“三教合一”，新旧教的教众已消除门户之见，作礼拜时再不选择教堂。我太太常去的那个教堂就有许多天主教徒鱼目混珠。只有陈大夫还在英勇捍卫教门的纯洁性），但另一方面他又口口声声“宽容”“和解”，要咱们停止批判党文化与伪民运那些毛共余孽，这到底是何高明道理？窃以为他恰好弄反了，不管是同性恋也好，兽奸也好，鸡奸也好，自摸也好，干卿底事？拜耶和华也好，耶稣也好，圣母娘娘也好，佛陀也好，安拉也好，撒旦也好，人家的自由，你管得着么？这些事毫不妨碍公众利益，个人根本就无权置喙。然而对真正败坏人民心智的毛共党文化及其现代代表人物，他却又奢谈“和解”与“宽容”，真不知该如何自圆其说。 <br />
<br />
另外，我对陈大夫的“基督教救国论”特别是反复叫卖的那什么四六不通的于歌的谬论也真腻透了，再说一遍：迄今为止，无任何证据表明民主的发生与基督教有任何相干。早在耶稣出世前，古希腊罗马就有了古典民主实践。现代民主的代议制基本上是从那儿来的，多出来的主要是人权观念，但这观念从《圣经》中找不到任何线索，它的来源一是从入侵罗马帝国的蛮族的封建契约制度萌生的法律权利（legalrights）观念，以《大宪章》为代表，二是洛克发明的自然权利（naturalrights）理论，我已经在旧作《再谈基督教在欧洲的勃兴和经典文明的衰落》中介绍过了。 <br />
<br />
从历史事实来看，现代民主制度在基督教国家的发生率甚至低于独裁制度的发生率。德国、意大利、俄国、南美各国都是基督教国家，种族政权统治的前南非也是基督教国家。其中俄国和德国甚至发明了人类历史上最反动的极权制度。我知道陈大夫立即要用上帝的面目出场，判定天主教和东正教不是真正的基督教，新教才是，但我也早就指出过了，纳粹德国信奉的是新教，南非信奉的也是新教。即使陈大夫再以上帝的名义把路德教打为邪教（或至少不是“真正的基督教”），从而成功地将德国开除出基督教世界而捍卫了理论的完美，则我也早就指出过原生（亦即不是靠外力强行植入而是自发产生）的民主国家里有一个恰好是信奉天主教的法国。因此，于歌之说，只证明了“信教使人愚昧，狂热地信教使人导波（double）愚昧，以救国的名义传教则吹波（triple）愚昧”。 <br />
<br />
总而言之，如今的时代早不是政教合一的时代，陈大夫错过了中世纪那“真正的基督教”的黄金岁月，堪称遗憾。 <br />
