																											<div id="postmessage_595309" class="t_msgfont">芦笛：为党的女儿柴玲同志说句公道话 <br />
<br />
<br />
网友spring（对了，老想问你，你到底是自称“春天”、是“泉水”，还是“弹簧”？）在拙文《不是学运“裹挟”柴玲，而是以她为代表的激进派劫持了学运》跟帖，说他被我说服了，但又怀疑柴玲是否有杀人故意，年纪轻轻，真有可能有我说的那么坏么？我看了之后觉得内心不安，因此再来追补上这篇文章。 <br />
<br />
柴玲当然极有心计，不是什么好东西，乃是天生的政客，绝非什么天真无邪的小姑娘。我那篇文章主旨不错。网友转贴来的刘刚的回忆就证实了戴晴早在90年代写的文章，而且说得更具体。5月27日，“爱国维宪各界联席会议”通过决议，拟于5月30日撤出广场，柴玲在会上一言不发，却于次日约谈自由记者金培力（PhilipCunningham），留下了那个历史性证言，旗帜鲜明地表示了她的反对态度，以便在未来证明她在革命成败的关键时刻反对了形形色色的阴谋家、投降派、特务与奸细的妥协投降、葬送出卖学运的罪恶主张。此后学领们根据联席会议的决定，在纪念碑那儿召集广场上的学生开会，动员他们撤出广场。王丹服从了会议决议，宣读了联席会议的十点声明。此前柴玲都不表示反对，但等到关键时刻却来了个突然袭击。据刘刚的回忆： <br />
<br />
“五月三十日撤出天安门广场的动员大会。我让刘苏里主持，让王丹念联 <br />
席会议起草的十点声明，我让吾尔开希重点强调一下这一个月来北京民主 <br />
运动所取得的重大成功，强调我们们的民主运动会尽快向全国各地延伸。 <br />
在王丹和吾尔开希讲话时，我草拟了一个字条，主要写了这次大会后将进 <br />
行由天安门广场出发的民主大游行，目的地将是北京的各大学校园。我同 <br />
时将全国的八大地区与北京的八大学校对应，比如说华北地区同北大对应， <br />
要求来自不同地区的学生都参与到对应的北京高校的游行队伍和以后的校 <br />
园集会活动。 <br />
<br />
要求北京各高校保障外地来京学生的食宿。我将我草拟的这份撤出方案， <br />
交给柴玲，让她照着念一遍就行。张伯立也要求讲了几句，他不过是又借 <br />
机宣扬一通他自封的天安门民主大学校长的身份，声称要坚持占领天安门 <br />
广场。我让刘苏里安排柴玲讲话。柴玲站起来，一手持话筒，另一手拿着 <br />
我的字条。她并没有按着我的字条宣布大游行路线和各地区进驻北京各高 <br />
校的方案。而是说：‘5月30日撤出天安门广场，不是我们广场指挥部的 <br />
决定，也不是我们坚持在广场的广大学生的意愿”，说到这里，她用手指 <br />
着我，又指指刘苏里和甘阳，继续说：‘而是他们，那些所谓的精英们的 <br />
意见’。这时我听到广场上一片喧哗，她后面再讲什么，我就全然不知了。 <br />
我当时只是想，在纳闷，这丫头也真会煽动，也真够老辣的，想不到在这 <br />
最后关头被这丫头给耍了。我还想，就是黄金荣杜润生再世，也没有这丫 <br />
头这胆识这能量啊。我让刘苏里尽快宣布大会结束。随后，甘阳似乎是担 <br />
心我们被激怒了的外地学生围殴，立即拉上我和刘苏里撤到政法大学的帐 <br />
篷内，甘阳还是气得直哆嗦，他猛然站起来，说了一句：‘我去抽她丫的。’ <br />
说完就向帐篷外冲，被我和苏里左右抱住才将他拦下。并不住地劝他：咱 <br />
们好爷们不同女斗。” <br />
<br />
来这阴毒的一手，老毛也不过如此吧？刘刚气糊涂了，没能听见柴玲后面说的话，那其实全在她与金培力的谈话中。幸亏她以为革命会成功，为了青史留名，特地留下了那份历史性证言，证明自己毫不妥协的坚定立场，这才有力地证实了戴晴与刘刚的指控的真实性。 <br />
<br />
柴玲既然反对撤出广场，认为“有人一再主张撤，这撤，唯一高兴的就是政府”，那她为什么不在会上和私下坚决反对，要等到正式召开动员大会才来此突然袭击呢？此中奥妙，她其实也在那个讲话中泄露了： <br />
<br />
“我悲哀的是什么呢？我是总指挥，我一再要求这个权力，掌握这个权力， <br />
就是为了抵制这种妥协，这种投降派。而且作为北京高联和外高联，外校 <br />
的高联，他们很愿意要这种权力。” <br />
<br />
“作为一些知识阶层的人，成立了一个知识……什么各界联合会议，爱国 <br />
维宪委员会，在昨天会上我很愤……愤慨，因为我感觉到这些人也是在利 <br />
用学运重新塑造他们的形像。我一再抵制这种倾向，象刘晓波把吾尔开希 <br />
（断）对不起，上面说的可以删一下吗？要推举他作发言人什么的。我在 <br />
运动中对这类人有些看法，吾尔开希，就是他，曾经利用他的影响，他所 <br />
处的那种领袖地位，对整个学运产生了很大的破坏作用。这已经发生两次 <br />
了，最后一次给我们坚决抵制，而且罢免了。但现在他们有些人要重新树 <br />
他这个形像。” <br />
<br />
“尤其可悲的是，有一些同学，有一些什么上层人士，什么什么人物名流， <br />
他们居然为了达到个人的目的，完成自己的一些交易，拼命地在做这个工 <br />
作，就是帮助政府，或者不让政府采取这种措施，而在政府最终狗急跳墙 <br />
之前把我们瓦解掉，分化掉，让我们撤离广场。 <br />
<br />
“如果是这种同学们自我崩溃，自我瓦解这样一种情况下，我们要，要撤 <br />
回原地的话，那么中国就会这样的一种情况：党内的所有的比较先进的什 <br />
么思想有点民主意识的人，还有历次运动中，象什么四五运动，象什么反 <br />
自由化，清除精神污染历次运动中没有被打下去的人，这次一下全被清洗 <br />
乾净。” <br />
<br />
这些话的意思可以大致总结于下， <br />
<br />
1）如果撤出广场，则学运成果就会被上层人士、人物名流、阴谋家、投降派、特务、奸细们篡夺，让他们成为政府的功臣。 <br />
<br />
2）如果撤出广场，就是学生们自我崩溃，使得政府失去了“最终狗急跳墙”的机会，学运就此被毫无结果地葬送，而她和其他人就会被政府清洗干净。 <br />
<br />
3）因此，必须坚决抵制这阴谋，在广大群众面前揭露之，把权力从上层人士、人物名流、吾尔开希、北京高联和外高联中夺过来，这不是为了自己，而是为了中国的前途。 <br />
<br />
很明显，如果柴玲只是在私下串连说服各界联席会议的头头脑脑们，并在会上据理力争，通过民主表决挫败“投降派”的阴谋，则投降派、阴谋家们的罪恶阴谋与反动面目就不会在广大群众面前暴露出来，仍然能对群众具有欺骗作用，因此，必须让他们表演得十足，然后在他们毫不提防的最后一刻加以致命的一击，煽动起群众对他们的无产阶级义愤，擦亮革命群众的眼睛，提高群众的革命警惕性，把权力从出卖学运的阴谋家投降派手中夺过来。 <br />
<br />
这在现代人看来当然非常邪恶，因此大众实在无法相信一个23岁的心理学硕士生竟然会有如此深沉恶毒的算计，如此丰富的权力斗争经验。可惜这是事实，而这种邪恶心术之所以显得邪恶，是因为今日大家都生活在西方，忘记了它乃是党妈妈手把手教会每个人的童子功。大环境就是那样，柴玲不过是千千万万个党文化学得最好，用得最活的毛主席的好学生中之一个罢了，又有什么难以想象、难以理解的？ <br />
<br />
我辈回首往事，不能“倒放电影”，用现代人的觉悟去回顾历史，而要用当时的人的文化心理去如实还原他们的心态。请记住，当时所有的国人无一人有西方文明常识，所有的想法都只能来自于党书籍，分析形势、拟想对策的全套思维方式都只能是党思维套路，革命家们当然只可能按照党教的方式去干革命。历史的讽刺在于，那场所谓的“民主运动”乃是我党写下的剧本，其中的所有角色都严格按照按党电影、党小说中的英雄人物的台词出演那场威武雄壮的活剧，忠实到了可以乱真的地步。鼓舞着柴玲等人英勇斗争的英雄形象，不是马丁&#8226;路德&#8226;金（她连那是什么人都不知道），也不是她看过的浅薄电影《谭嗣同》，而是卢嘉川、江华、林道静、许云峰与江姐们。 <br />
<br />
九十年代我在《华夏文摘》上看到一份报告文学，讲的是一位姑娘怎么怎么机智勇敢地掩护救助被政府通缉的王军涛，像煞了我自幼便读熟了的革命故事（特别是《红岩》），当下又是感动又是啼笑皆非：鼓舞着新时代青年去与我党斗争的英雄形象与效法榜样，竟然是老一辈坚贞不屈的无产阶级革命家，而他们与“共产党反动派”斗争的灵感，竟然来自于党小说、党电影中描写的革命先烈与“国民党反动派”斗争时使用过的机智勇敢的战略战术！ <br />
<br />
这就是柴玲可以原谅的理由：她这位“杰出的民主斗士”不过是个乔装改扮的共产党人罢了。因此，她真诚地反对一切妥协，因为那在党字典里就是投降，真诚地期待（也就是盼望与等待）我党狗急跳墙，对着人民举起屠刀，让广场上血流成河，因为只有这样才能擦亮人民的眼睛，唤起他们起来和法西斯政府较量，让革命成功。她看过的所有政治读物都是这么教诲她的，她又岂能有另类思维方式？至于血流成河到底是什么景象，人民又如何才能以赤手空拳与武装到牙齿的兽军较量，这些问题从来不会出现在她头脑中，因为所有读物中没有一本讲过这个问题。 <br />
<br />
所以，谴责柴玲心地邪恶当然不错，但问题在于，那就是她知道的干革命的唯一方式，她非但不知道别的方式，而且还把自发产生的不邪恶的方式真诚地看成了邪恶。国家的整个教育都是邪恶的，她不过是个背书背得比较流畅的好学生罢了。这就是整个悲剧中最令人不寒而栗之处。遗憾的是，直到今天，也并不是所有的人都能清晰地看出这点来。 <br />
<br />
当然，再进入角色、表演得再真诚的演员，其表演也不可能没有私心杂念。柴玲的稚气，在于她居然把自己是革命火种，是指挥诺曼底登陆的艾帅，因此决不能轻易牺牲的台词也背出来了，而这就是她与那些革命老前辈的区别。 <br />
<br />
须知社会上除了党教育外还有其他私人来源。那个时代的青年从小就听过长辈讲述我党引蛇出洞、秋后算账的“阳谋”。因此，柴玲内心深处潜藏着对党的深深的恐惧，害怕运动灰溜溜地结束，很不甘心在黑牢里蹲上17年，放出来时就40岁了，这也是很自然，可以理解，值得同情的。 <br />
<br />
出于这种绝望的恐惧，煽动百姓堵军车，让她和其他人可以无限期赖在广场上，也就是她能唯一想得出来的招数。因为不幸的是，革命并没有像党神话中描绘的那样发展，她看过的所有读物都没教过她如何既能保证自己安全，又能体面结束运动的策略，当然就只能将求生的希望寄托在万能的人民身上。就连这招也是党教给她的：当年的八路军好汉们之所以能躲过鬼子的刺刀，不就是全靠乡亲们保护么？ <br />
<br />
这儿的悲剧在于，尽管柴玲口口声声识破法西斯政府的本质，斥责与我党妥协是“作梦”、“白日作梦”，但她对党的本质根本没有最起码的理解。我党几十年的 “人民政府”、“人民军队”的欺骗宣传功夫并没有白费。虽然她口口声声“期待流血”，但我深信她不过是在机械背诵党台词罢了，其灵感很可能来自于邓小平在 86学运时说的“我们不怕流血”。在内心深处，她可能根本就没去想过、也无法想象那是什么意思。我记得她在事后传出了一份录音带，上面说她原来最坏的设想也就是派人把他们从广场上拉出去，没想到会如此滥杀一气。我相信这很可能是她真实的心声。 <br />
<br />
因此，事实是： <br />
<br />
1、如果学生按各界联席会议在5月27日作出的决定于5月30日撤出广场，则大屠杀就不会发生。 <br />
<br />
2、柴玲在动员学生撤出广场时违反她参与的各界联席会议的决议，采用突然袭击的方式，在刘刚让她宣读撤出方案时，以广大学生代表人的身份宣布抵制该决议，并煽动学生反对执行该决议的刘刚等人。 <br />
<br />
这就是柴玲在历史的关键时刻出于自由意愿作出的表演。当然，我反复说过，群众运动若没有一个列宁党在幕后操控，就只能是刘刚无限痛苦地发现的“布朗运动”、“不可控热核反应”。即使柴玲忠实执行了各界联席会议的决议，学生也未必能听她的指挥撤出广场，从而避免六月三日的大屠杀。但不管怎么说，她必须为她自己在关键时刻的表演承担历史的责任。 <br />
<br />
这不是说她有杀人故意。上面已经说了，我倾向于认为她的“期待流血”之类的大言壮语不过是机械背诵党台词，并没有去仔细想过那是什么意思，也不相信共党真会这么干。退一万步说，即使她真有这意愿，那也不能构成我党滥杀平民的理由。学运从头到尾都是和平抗议示威，别说与剐了宫中太监的北宋学运相比，就连五四运动中火烧赵家楼、痛打章宗祥、30年代共党操控的学生冲入政府部门打伤外交部长与教育部长之类的过火行为都没有。 <br />
<br />
说到底，作为学运的激进领袖，柴玲的道德责任只在于为了一己安全，不但坚持赖在广场上，而且发起市民“保卫天安门”的活动，在客观上成全了当局的大屠杀。但这行为本身并不构成对当局统治的威胁，毫无武力镇压的必要，更不是血洗京城的借口。柴玲的失误只是低估了中共政权的凶残，未能预见到她的求生行为将给人民带来何等深重的灾难。但对于一个党的好女儿来说，犯这种错误是很自然的。更重要的是，她的错误不但是党教育直接造成的，而且也只有在空前黑暗的中共统治之下才成其为错误。在任何一个比中共极权统治稍微文明一点的威权国家，诸如过去的台湾、南韩等国家，这种行为都不至于引起灾难性后果。 <br />
<br />
但这不能成为伪民运人士维持“八九民运”神话，在海外继续造神活动，封杀批评，制止对六四国殇进行客观反思、总结经验教训的借口。迄今为止，我对柴玲这个曾经深刻影响了亿万中国百姓生活的公众人物的批判，都是着眼于还原历史事件的真实面目，揭示共党统治的凶残，以及使用共党那套不知妥协、大而无当的政治斗争去对付共党可能招致的恶果，强调的是新时代的革命家们必须从共党空前的凶残性着眼，加强“责任伦理”观念，在号召人民行动时，必须吸取八九学运的沉痛教训，时时处处以苍生性命福祉为念，民之祸福，长在我心，不要再像方励之那样哗众取宠，轻率煽动人民起来“以斗争争取民主”，也不要像柴玲那样，为了自己的巨大荣誉不被“上层人士”、“头面人物”们窃夺，为了逃避17年的黑牢生涯，就轻率号召市民“保卫天安门”，期待广场上血流成河。 <br />
<br />
不幸的是，并不是所有的人都这么想。20年来，我一直期待着当年的学运领袖出来真诚地向人民道歉谢罪。然而，这些人以及伪民运人士的毫无心肝的反应激怒了我。如果说20年前的柴玲还可以原谅，如今的柴玲还要坚持维护她的神话形象，为此不惜动用她的雄厚财力搞垮一家非盈利组织，只不过是因为人家拍的电影忠实记录了她的心声，那就绝对无法原谅了。更不能原谅的是，在八九学运发生之后，对我党可以凶残到何等地步，海外人士都有了最起码的感性认识，伪民运头子居然还有本事在网上盛赞“我们的英雄杨佳”，呼唤“大泽乡起义”！悠悠苍天，此何人哉！需要多少年的邪恶文化积累，才能锻造出这种毫无心肝的“民主斗士” 来？！ <br />
<br />
不知道珍惜民命的“民运”只该破产，以民命为成功代价的野心家们只配被万众唾弃。只有当这形成国人的普遍共识后，我们这个民族也才能算是成熟了。 <br />
