													<h2>辛亥革命是一场什么样的革命？</h2>
						
						
																											<div id="postmessage_535026" class="t_msgfont">辛亥革命是一场什么样的革命？ <br />
―― 和女儿讨论中国近代史引发的随想 <br />
<br />
杨丹荷&nbsp;&nbsp;<br />
<br />
几年前，和女儿一起观看了朋友赠送的《走向共和》光盘。当时刚上高中的女儿说：“中国为什么不能保留皇帝？为什么要革命？”我不假思索地说：“辛亥革命当然是对的。中国建立了亚洲第一个共和国，推翻了几千年的封建专制。你小小的年龄，怎么这么保守？难道你还想要恢复帝制？再找张勋领一帮辫子兵回来，上演一出复辟的闹剧？这不仅绝对不可能，而且很可笑！”女儿说不过妈妈，但是仍然坚持说：“他们真笨，当时为什么不保留皇帝、实行君主立宪？”<br />
<br />
不久前，我们又讨论起这个问题。女儿高中时随“音乐大使”演出团队去了七个西欧国家，历时两个多星期。去之前，她上了一年西方美术史，阅读了有关国家的艺术文化，她非常爱好意大利足球，对其文艺复兴时期的成就深感兴趣。她制作了一个2006年意大利队获得世界杯冠军的射门集锦，放在互联网上，某些意大利人看了极为感动。她也喜欢法国的艺术文化。不过，旅行回来后，女儿说她此行最喜欢的国家还是英国。在政治上文化上，英国是稳健改良的典范国家，它对传统文化的妥善保留、它的不流血却极其成功的“光荣革命”，它建立起来的体现自由主义理念的政治制度，都是值得他国借鉴学习的典范。女儿说，在她的经验中，英国最有秩序，最文明，最富有传统文化色彩。女儿喜欢北京，尽管小小年龄便移民北美，她仍然认为自己文化的根在中国。年轻人一般追新、求变，女儿却希望她印象中的北京不要变。<br />
<br />
这些年，我一直在独学研究，十分关注中国近现代思想和政治文化演变，花了很多功夫阅读研究有关史料。几天前，女儿又谈到：“为什么中国不能保留帝制？中国的传统文化都没有了，中国不应该有激烈的革命。”我真的开始同意她的观点。她当然没有做有关研究，但是她比较有头脑，能“想”问题，会分析思考， 而我这个妈妈则比较轻信盲从，一向是被怎么灌输，就怎么相信，虽然专业是“哲学”，实际上多年来，不会运用自己的理性分析，不会独立思考，不会阅读原始材料，摸清基本事实，而是吃“别人嚼过的馍馍”（大学本科时，一天，在北大三角地看到一位外来者，在那里咒骂北大的学子们不过是在吃“别人嚼过的馍馍”，对此印象极深），没有掌握真正的治学方法，浮躁空泛。<br />
<br />
女儿是个小姑娘，她在政治文化上的保守立场却足以成为严复、王国维和陈寅恪这几位近代中国的真正文化大师们的后代知音。我想，如果真的灵魂不灭，真的有在天之灵（对此，我只能悬置，不作判断），这几位令我深深尊敬的学者，得知女儿的看法，一定会露出宽慰会心的笑容。他们大概会说：“小姑娘，我们很高兴，你小小的年龄，就能理智不偏激地思考问题。我们完全同意你的观点，我们认为，辛亥革命是个错误，上个世纪中国本来可以避免激进的政治革命，可以更加顺利地引进西方文化，不必经过那么多的动荡、战乱、流血，付出那么大的生命代价。激进政治和文化革命的结果是，陈独秀高喊的‘德先生’和‘赛先生’至今还是拒绝在中国安家。看看普遍的乃至渗透全部学术界的腐败行为，以追求真理和事实为己任的‘赛先生’在哪里？看看民众生命财产人身安全和其他权利没有着落，空洞浮泛的“德先生”变成了“人民民主专政”，即以“人民”和“国家”的名义剥夺个体人权自由。英国有个受到民间尊重的王室，并不妨碍它的国民享有自由民主，而中国的激进革命废除了王室，连传统主流文化儒释道三家也全面遭殃。正因为中国传统主流文化的全面断裂，外来自由主义没能顺利引进，才给了激进暴烈崇尚斗争仇恨的外来马克思主义以可乘之机，导致了长期的专制暴政，上演了‘无产阶级文化大革命’的历史悲剧。自从辛亥革命发生，一个世纪过去了，至今中国仍然没能完成自由主义政治的引进。自由、平等、宪政，仍然不是中国的政治现实。”<br />
<br />
如果尊重历史事实的话，就应该承认，1911年中国的辛亥革命其后果虽然建立了一个形式上的“共和”国家，在时间上抢占了亚洲第一，其实这个所谓的“共和”国，正如陈寅恪所言，非驴非马，乃是个假冒伪劣产品，其后的一次次政治革命更是每况愈下。<br />
<br />
中国的辛亥革命，模仿目标本来应该是美国，结果却很像法国的大革命，众多人头落地，血流成河，换来的结果却不伦不类，适得其反。在法国大革命之前，美国也发生了背叛自己祖国的流血政治革命，“自由”和“平等”理念深入人心，战胜了对祖国的民族主义情感。美国本来是英国的殖民地，在政治上隶属英国，在文化传统上延续英国，但是一旦涉及“自由权”问题，便“不自由，毋宁死”，美国革命完全超越了血缘政治和民族主义。“自由”高于一切。难怪美国的国歌歌颂美国是“勇敢者的家园，自由者的土地”，难怪在纽约港的入口处，高耸着自由女神的塑像。美国本来是欧洲殖民者的新家园，在发动革命时，自由平等理念已深入人心。美国革命成果的确体现了“自由”和“平等”政治原则，美国《独立宣言》完全体现了自由主义思想之父洛克总结的政治理念，体现自由主义政治原则的美国宪法是国家的最高法律，若不是政治平等原则起作用，女人希拉里和黑白混血儿的奥巴马就不能参加总统竞选。更显黑色人种基因的奥巴马成了总统，是“人人生来自由平等”理念的胜利。所以，美国的革命值得，革命先烈们的血的确没有白流，美国的共和体制货真价实。美国的国父们，的确是值得后辈永久尊敬纪念的伟大政治家。自从这个国家成立以来，世界各地的人们或者为了自由、或者为了幸福、或者为了安全，蜂拥而至，孔子所向往的人民携妻带子纷纷投奔的政治盛况，二百多年来一直是美国的真实现实，这就说明，美国的革命的确“合乎天理，顺乎人心”，革的好，革的对。当年高唱“无产阶级文化大革命就是好”、后来移民美国的红卫兵小将们，应该高唱“美国革命就是好。”<br />
<br />
中国的情况则不然。认真研究一下中国的“革命先行者”孙中山，便能发现，他其实不是自由主义者，无论在人格上还是在思想上，孙中山与美国的国父们都无法比肩。华盛顿对美国的功绩与孙中山对中国的功绩无法相提并论。参观美国国会山时，见到华盛顿向国会交兵权的巨幅画作，几乎泪下，这才是“心底无私天地宽”、以“天下为公”的真正政治家，尧舜禹以后的中国历史上从来没有出现过这样的政治家。数年前，亲耳听到中山大学近现代思想史专家袁伟时教授介绍，说孙中山及其同盟会成员，其实很象社会上的团伙帮会，真正了解他们为人处事的人，很难崇敬他们。 孙中山真正的思想动力不是个人自由，而是推翻满清的狭隘民族主义和大政府、土地国有等社会主义经济理念，自由主义坚定捍卫私有财产权和经济自由，与孙中山信奉的那一套不搭界。民主共和是孙中山用以推翻满清王朝的招牌而已，自由主义政治到底是什么他并不真的清楚，所以，中国的自由主义引进者严复跟孙中山接触交流后，不仅不同意他的政治革命主张，也相当鄙视孙的为人。北洋军阀和国民党政府都不是基于与美国立国同样的政治原则建立的。中国的“革命”和“共和”与民众的个体自由和政治平等毫无关系，老百姓不过是被利用的“群盲”而已。鲁迅的《阿Q正传》生动形象地描述了中国近代革命中的老百姓，不过是被愚弄的贱民而已。“人民”和“民主”是政客们发动革命、夺取政权的金字招牌。中国人很会造神，尤其是会造假神，自己被政客们涮了，反过来还要膜拜他们。<br />
<br />
此外，中国有悠久的帝制历史，不像美国那样一清二白，可以按照自由主义政治理念建立崭新的共和政体。中国应该在形式和实质上学习英国，而不是形式上模仿美国，实质上却步血腥极端的法国大革命后尘。革命，正如王国维当年预见的，只是给政治野心家和政客们提供了大展身手的历史舞台，群众，不过是成了他们的可以用来达到目的的“利器”。王国维为什么要自沉？没有积极意义的辛亥革命已经令他相当不满，军阀冯玉祥不顾民国优待清室的明确法律条文，轻率驱逐废帝溥仪出宫，更令他痛感所谓的“民国”，实则不讲道义，无视法律，继之而来的国共联合北伐，则让他预见到比辛亥革命有过之而无不及的革命行将来临，人格尊严意识极强的他，理性地毅然自沉。自沉时年方五十，正值学术生命的旺盛时期。四十年后，他的学术知音、同样富于理性和道义感的优秀学人陈寅恪悲凉逝世于“横扫一切牛鬼蛇神”的“无产阶级文化大革命”高潮阶段。<br />
<br />
王国维和陈寅恪的人生悲剧，正是中国近代政治和文化悲剧的缩影。悲剧的根源是没有正面价值意义思想所引导的政治和文化革命。<br />
