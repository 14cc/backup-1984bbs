													<h2>飞虎队：也许西方文化确实不适合于中国人</h2>
						
						
																											<div id="postmessage_506301" class="t_msgfont">有一次，我给一个国外的朋友打电话，因为很久没跟她联系了所以很想跟她说话，就莽莽撞撞地打了过去，但没想到她还没起床，于是她拿起电话来骂了一句就把电话挂了，从此就翻脸绝交了。 <br />
<br />
我是百思不得其解，至于为了这么点小事就翻脸不认人了吗？ <br />
<br />
第二天早上我醒来时，没怎么睡醒躺在床上很疲惫，也确实感觉到哪怕天塌下来也不想去管它的只想一直睡下去，感到自己确实有鲁莽之处。 <br />
<br />
但若有熟人在我睡觉的时候打电话过来，我是怎么都没有那个胆子不顾一切地跟他翻脸不理不睬的，我也不知道我是生来就这么胆小还是曾经我行我素过但遭遇挫折太多所以不敢再任性，也许我确实是一个不可救药的支那豚吧。 <br />
<br />
由此我注意到，很多比较西化的人似乎都有一个特点：那就是有时虽也很热情，但一旦不高兴起来也很容易翻脸不认人的。 <br />
<br />
我还不敢说这就一定是西方人的特征，因为我其实也并不很清楚真正的西方人是怎样的。但我同时也注意到，西方人很注重于对于自己个人利益和个人权利的维护，有时到了一种敏感和激烈的程度。那么这种现象是否与此有关呢？还是只是中国人自己的劣根性导致的橘生于南则为橘，橘生于北则为枳的怪异现象？ <br />
<br />
但现实生活中中国人一般来说不会随便为小事跟熟人翻脸（但对陌生人就特别残酷），甚至哪怕是很大的利益问题一般也不轻易翻脸（但下来以后在背后却报复得厉害），我现在倒也不倾向于把这个看作是中国人虚伪阴险的原因，但是当然也不是因为中国人特别仁慈宽厚的原因，宁可中立地认为主要是因为在中国社会中人们普遍担心得罪人会受报复的心理。 <br />
<br />
现在不是毛泽东那个时代，也不是朱元璋武则天那个时代，其实并不是说你一不小心得罪了谁他就一定会纠缠不休处心积虑千方百计地蓄意整治你报复你，现时代人们主要的心思都在忙于挣钱上，没有毛泽东时代那个闲功夫了。但是这绝不是说你就可以随便得罪人而不用担心会遭到任何损失了。 <br />
<br />
中国社会跟西方社会比应该有这么几个不同之处，一是中国社会的法制化程度不高，不够牢靠，人与人之间的利益关系更多的只能依靠人际关系网来维持。于是，如果一个人得罪人太多，而又没什么强大的靠山，那么，即使你说你并没有违反什么法律，并没有任何不符合道德之处，但你的人际关系网一旦破灭或者恶化，人人都不来理睬你了，你在这个社会中就寸步难行了。因为即使别人并不主动来整治你，但在这个社会中人人都得靠关系生存，你没有了这种关系网，你相比较别人而言你就处于生存竞争的劣势了，就会被挤到边缘地位上去。 <br />
<br />
如果西方人勇于跟人翻脸勇于得罪人（假设是为了维护自己利益），我想应该也是因为西方社会强大的法制化或者说在社会生活中方方面面都有极其完善的制度性的保障机制，使得人们不必为了生存违心地去事事苟全求同，反正我有其他的途径可以去实现自己的利益，比如说诉诸法律啊，结社组党啊，自己创业啊，隐居遁世啊，怎么都行，自由空间大得很，我不必非得勉强自己去维护跟某个自己不喜欢人的人际关系。 <br />
<br />
但在中国你就逃无可逃。 <br />
<br />
当然，这也许只是我的一种猜想（甚至连前提都不可靠），因为我也注意到现在在西方社会中“政治正确性”的桎梏越来越严酷，使得人们越来越不敢随便乱说乱动作，（其实这个政治正确性我认为很大程度上正是因为东方势力的渗透造成的），但似乎这种现象一般还仅见于对公众人物的限制上。 <br />
<br />
中国文化讲的是“存天理灭人欲”，我是一贯地本能地反感这种不人道的文化，难道人欲的得到满足不就应该是最大的天理吗？（仁者见仁智者见智吧） <br />
<br />
当然实际上中国人自古以来肯定是不排斥人欲的满足的，但那仅仅只限定于掌权者得到满足，实际上这个所谓的天理（灭大众的欲）就是直接为满足掌权者的沟壑难填的“人欲”而服务的。 <br />
<br />
在中国只要你有了权，人人都会来无微不至地关怀你，也许我这种观点看起来跟现在网上铺天盖地的人肉贪官的现象有所不符，但那只是网络上偏颇的假象，在现实生活中我几乎从没看见有哪个人敢于跟自己的领导顶撞的，相反只要有领导出现的场合，人们都会或主动或被动地本能性地迎上前去恭维示好，其实这是一种不是人的意识能够控制得了的生存本能，我倾向于这么认为。 <br />
<br />
然后到了网上，蒙着面，人们就开始表现出自己的另外一面，疯狂地发泄自己对社会对他人的怨恨，但通常也只能在网上发泄。 <br />
<br />
西方社会自古以来则强调个人利益较多些，到了现代，个人主义自由主义更是蓬勃发展，甚至到了有些极端的地步以致遭受诟病。 <br />
<br />
我不好说这其中利弊如何，但我渐渐明白，西方文化之所以能成其为西方文化自有它的历史必然性，因为从历史条件上来看西方世界有能够给其提供丰厚土壤的生长环境。 <br />
<br />
比如说，西方的人口密度，自古以来没有中国那么密集，相对稀疏的人口密度当然是一个能够保持人与人之间利益平衡的重要条件，因为生存空间更大了，生存资源平均下来更多了，相互之间发生摩擦的距离拉远了，从而摩擦频率也降低了。 <br />
<br />
我以前看布拉德皮特演的《秋日传奇》（好像是这个名字），我就感到很惊讶，何以西方人能够那么勇猛地敢于跟政府对抗，又何至于有那么激烈的拒斥任何政府管束的心理。可能就是因为地广人稀，人人家家都有一块大得看不到边走不到头的土地，自己物质既不缺乏，可以自给自足，而又少与周围人接触，当然日久就很易滋生独立王国的野心（中国俗语说“穷山恶水出刁民”大概也有这个因素在内）。 <br />
<br />
以前我就曾经感到困惑：何以很多西方人看起来高尚得简直就像是傻X，现在我慢慢明白：那也得我先吃饱了才能来施舍你对不对？ <br />
<br />
其实，西方文化相比较中国文化而言，虽然是一种高质量的文化，但同时也是一种需要高技术，高成本投入的文化。 <br />
<br />
如果我们把文化定义为一种生活方式以及思维方式的话（而不是简单化表面化的琴棋书画之类死东西），那么，首先，西方式的思维是以数理逻辑为基础，这个在技术上就要比以玄想类比形象思维为基础的中国文化高阶复杂。 <br />
<br />
其实我发现一个有趣现象，往往理工科的人思维方式更西化一点，文科的人虽然嘴巴上经常也大谈西方文化追捧西方文化，但却经常表现出东方式的思维，而且很容易从一个极端跳到另一个极端（比如说在现实中受了挫折生活不顺时就大骂中国仰慕西方，一旦进入了体制内成了既得利益者又马上反过来吹捧中国大骂西方，最典型的就是以前写了《耻辱者手记》现在又写《中国站起来》的那个文人摩罗） <br />
<br />
而西方在现代科学基础上和工业化信息化基础上发展起来的高度物质化生活方式，也比原始土冒的中国小农生活方式成本要高得多，要消耗大量物资和能源才能维持。 <br />
<br />
于是现在整个国际社会都在纷纷指责美国方式消耗能源资源太大不足以成为普世方式，呼吁大家回复绿色环保的自然生活方式，当然好笑的是，人人都是只要求别人不要求自己的。 <br />
<br />
这就又牵扯出另一个问题，那就是现在的中国文化到底多大程度上还是真正的原初的中国文化？ <br />
<br />
实际上现在的中国文化是一个不伦不类的四不像。 <br />
<br />
不是说文化就不能发展进化，但中国文化一直以来被其鼓吹者引以为自豪的是其所谓的“传承性”，其实从另一个角度来看，这正是因为中国文化两千年来并无变化长进的原因，一直在原地打转循环，一直只是简单内容的肤浅重复，所以感觉“博大”（垃圾特别多嘛），但细查则并无精深之处。 <br />
<br />
但好笑的是，就是这样，现时代的“中国文化”也根本就是个不伦不类地混杂了中外古今各种糟粕的大杂烩而已，好的东西则是一点都没学到。 <br />
<br />
所以我感悟到，说自己热爱中国文化的未必是真热爱中国文化（也许连到底什么算是中国文化都不清楚），说自己热爱西方文化的也未必是真热爱西方文化（也许连到底什么算是西方文化都不清楚），但人们肯定是热爱现实利益的（但很多时候人们又往往弄不清楚自己的根本利益何在，或者分不清眼前利益和长远利益）。 <br />
<br />
于是，也许很多关于“文化”“人文”（其实这个舶来的西词本意是专指人道主义，但现在中国人习惯于将一切跟吃喝拉撒有关的东西都冠以“人文”的大帽子）的喧嚣都只是痴人说梦吧。 <br />
<br />
如果从技术上和物质条件上中国注定了难以移植西方文化（当然如果有一天人们有了那个决心要真正西化，我也是不会拦着他们的，但现在不谈优劣，只看有没那个可能性），那么我们还是安心地做我们的黄皮猪吧。</div>

							
							
							
							
