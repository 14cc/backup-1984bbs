													<h2>八议制度：中国古代公然规定“法律是保护官员和权贵”的传统</h2>
						
						
																											<div id="postmessage_377769" class="t_msgfont">中国司法自古以来就反对法律面前人人平等的精神，最著名的如“八议制度”，它的存在充分说明了中国几千年从来都是公然的保护特权等级，更没有出现过司法独立的情况，王权将行政立法司法合一，各类贵族、官僚均享有天然的法律赦罪特权，在司法上体现儒家专制特权伦理。<br />
八议制度是中国古代刑律规定的对八种人犯罪必须交由皇帝裁决或依法减轻处罚的特权制度，源于周代的“八辟”。 <br />
　　八议入律，始于三国曹魏将八律载入《新律》，后历代沿袭。明清时，八议制度有变化，取消“流罪以下减一等”，保留权贵犯罪一律由皇帝裁决的规定。<br />
　　八议具体包括：<br />
1.议亲，指皇室一定范围的亲属；<br />
2.议故，指皇帝的某些故旧；<br />
3.议贤，指朝廷认为“有大德行”的贤人君子，如其言行可供人效仿的人；<br />
4.议能，指“有大才业”，能整军旅、莅政事，为帝王之辅佐、人伦之师范者；<br />
5.议功，指为王朝建立过卓著功勋得人；<br />
6.议贵，指职事官三品以上、散官二品以上及爵一品者；<br />
7.议勤，指为封建国家勤劳服务的人，如日夜操劳、恪守职责的将军。<br />
8.议宾，指被尊为国宾的前朝国君的后裔。<br />
<br />
　　上述8种人犯了死罪，官府不能直接定罪判刑，而要将他的犯罪情况和特殊身分报到朝廷，由负责官员集体审议，提出意见，报请皇帝裁决，给予宽宥处理。<br />
<br />
一般情况下死刑均能免除。犯流以下的罪，要减一等论罪。而几千年来，普通的中国平民百姓是没有这些司法特权的。<br />
&nbsp; &nbsp; 八议制度使贵族官吏的犯罪处罚能够大事化小，小事化无，以致使犯罪者完全逍遥法外。而这一切均是在合法的旗号下进行的，是特权法的典型反映。中国古代的法律是特权法，意味着其内容贯穿着不平等的精神和原则，按照人的不同身份、不同社会等级来确定相应的法律地位，是宗法等级社会的必然产物。而传统宗法观念的核心，即是认为社会成员有高低贵贱的等级区别，不同等级的人享有不同的权利，承担不同的义务，每一等级的人都必须严格遵守，不能逾越。而八议制度的出现，也是出于君主帝王巩固其统治的需要，君主的统治要维系下去，就必须依赖一个服从与效忠他的贵族官僚集团，因此君主必须要采取一些笼络的手段，给予他们一定的优惠。将贵族们的特权用法的形式明文确定下来，无疑是一个非常有效的方法。<br />
<br />
中国古代公然将“法律面前人人不平等”写进律条里，这种传统与文明社会的“法律面前人人平等”原则格格不入，也是中国几千年来王权专制垄断一切权力，有王法却无法治的集中体现。<br />
