中国封建刑律对于 8种权贵人物在审判上给予特殊照顾的制度。对于这些权贵人物来说，就是一种特权。 <br />
<br />
八议包括议亲、 议故、议贤、 议能、议功、议贵、议勤、议宾。“亲”指皇室一定范围的亲属；“故”指皇帝的某些故旧；“贤”指朝廷认为“有大德行”的贤人君子；“能”指“有大才业”,能整军旅、莅政事,为帝王之辅佐、人伦之师范者；“功”指“有大功勋”者；“贵”指职事官三品以上、散官二品以上及爵一品者；“勤”指“有大勤劳”者；“宾”指“承先代之后为国宾者”（《唐律疏议・名例》）。这8种人犯了死罪,官府不能直接定罪判刑，而要将他的犯罪情况和特殊身份报到朝廷，由负责官员集体审议，提出意见，报请皇帝裁决。这8种人犯流以下的罪,都要减一等论罪。唯一例外是，如果他们犯十恶罪，则不适用上述规定。 <br />
八议制度起源于周代。《周礼・秋官・小司寇》有“以八辟丽邦法”的记载。 辟即法， 八辟即议亲之辟、议故之辟、议贤之辟、议能之辟、议功之辟、议贵之辟、议勤之辟、议宾之辟。秦用（《九朝律考》）。商鞅变法，强调“一赏、一刑、一教”,“刑无等级,自卿相、将军以至大夫、庶人，有不从王令、犯国禁、乱上制者，罪死不赦”（《商君书・赏刑》），因此没有在刑律上对特殊人规定特殊照顾的八议制度。汉承秦制，也没有八议制度，但有对高级官吏犯罪给予特殊照顾的事例。汉高祖八年（公元前199）“春,令郎中有罪耐以上，请之”（《汉书・高帝纪》）。以后在宣帝、 平帝、 光武帝时都有类似诏令。东汉时屡有八议的议论（《九朝律考》）。在律典内明文规定八议，是从魏开始。晋、宋、齐、梁、陈、北魏、北齐、北周、隋、唐、宋、辽、金、元、明、清都有八议。但各朝规定不尽相同。大体说来，隋较唐宽，宋抄袭唐，明、清则转趋于严。唐、宋的君主比较重视八议的规定,为的是防止人们“有轻吾爵禄之心”(《宋史・刑法志》)。唐贞观二年,岐州刺<br />
史郑善果犯罪拘囚，太宗提出善果的官品不低,不应与诸囚为伍,并要求自今三品以上犯罪听于朝堂俟进止。宋政和间有品官犯罪、不得和常人一样加讯的诏令。 <br />
明、清则不同。 清钱大昕《十驾轩养新录》载：“明名例律虽载八议之条，乃戒治狱官勿许引用”，《大清会典》有“八议之条……不可为训”的话。清雍正六年三月，世宗给内阁的谕旨说：“八议之条……我朝律例虽仍载其文，而实未尝照此行者，盖有深意焉。”所谓深意，实际是要求显贵率先奉法，一切听命于皇帝。历史表明，在封建国家发展的过程中，王权伸张总是通过剪除或削弱权势炽盛的达官显宦来达到的。封建末期的明、清王朝，要竭力巩固君主专制权力，因此不重视以维护达官显宦为目的的八议制度，是很自然的。</div>

							
							
							
							
