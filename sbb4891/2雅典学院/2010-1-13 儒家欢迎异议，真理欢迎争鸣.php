<br />
有人指责东海“不允许别人也议论你所崇尚的儒家思想”，不是误言就是谣言。东海儒家从来没有“不允许别人议论”，相反，对别人的议论和批评持十分欢迎的态度，曾一度经常在海外转发批评东海、批评大良知学与良知信仰的文章，也常会对别人的错误知见、对不合理、不恰当、不中肯的批评进行回批，这与不允许别人议论是两回事。<br />
<br />
真理不怕光明，真理就是光明，真理充满自信，故真理不怕争鸣，不怕批评，不仅不怕，而且欢迎。真理越争越明。只有邪知邪见伪道伪理，只有狭隘低弱乃至反动黑暗罪恶的东西才是不自信、怕异议、怕见光的。<br />
<br />
对于无知误解误批，如理如实地给以澄清，则是文化责任感使然，是弘道解惑的必然。至于有意歪曲、恶意咒骂，于真理何伤，于真人何伤？“伤害的反而是歪曲咒骂者自已啊。这类声音不值得挂怀，这类人士则值得怜悯。<br />
<br />
唯有对言论自由的侵犯和剥夺，才能对真理造成“伤害”，但这种“伤害”终究是暂时性、局部性的。真理一旦发现，就会不断发光，就象太阳一旦上升，不可能永被乌云遮蔽。<br />
<br />
害怕批评、害怕异议、害怕争鸣、害怕言论自由及科学进步的，需要武力或强制力才能维持的，绝不是真理。在现代社会，违背“以言论对治言论”的文明原则对批评异议进行各种形式的封杀及迫害者，绝不是真儒家，也绝不是文明的人、文明的组织（包括政权及信仰团体）。一些基徒热衷于删批评之帖、封异议之口，正好暴露他们的不自信、不真实、不文明。<br />
