													<h2>中共越来越自信</h2>
						
						
																											<div id="postmessage_512010" class="t_msgfont">昨晚同事问我：新华社的消息怎么把人大代表和政协委员的行政职务也报出来了？（比如广东省省长黄华华代表）。我说这是中共自信的一种表现。<br />
<br />
众所周知，过去报道两会时，代表委员都是只说姓名，最多加个地域和界别，没有点明（准确说是隐去）行政职务的。这样做的原因是，西方老是诟病中国没有三权分立，如果点出代表委员的行政职务，恰好印证了这种指责。所以，隐去行政职务，起码表面上可以达到不留直观把柄的目的。但，地球人都知道，真正的狐狸尾巴还是藏不住的。<br />
<br />
此次公开报出代表委员的行政职务，说明中共根本无惧直露那个狐狸尾巴了。原因何在？在于胡鞍钢早先发表的那篇文章（名字不记得了），内容大意是：西方国家除了美国一国严格实行了三权分立以外，其他发达国家的三权并不分立（比如英国法官由议会任命，首相无权制衡）。不得不承认，事实的确如此。于是，中共的心态变了：既如此，我朝行政官员同时出任人大代表或政协委员，并在新闻中报出来，有何不可？此一。<br />
<br />
二，自从《民主是个好东西》一文发表后，其讨论结果似乎反而走向了该文作者及其支持者的反面――民主的弊端被越来越多的人所认识和承认（美籍华人经济学家方绍伟对民主的批判尤其鞭辟入里）。这些人中，既有中共高层官员，更有不少知识分子。就知识分子而言，其中一些简直可谓幡然醒悟（比如摩罗）。有了对民主的重新认识，中共抖擞一下被指责为独裁的身板儿――我就是三权不分立（让行政官同时担任立法者）、我就是要走有中国特色的道路，谁能把我咋样？！此二。<br />
<br />
三，08宪章反响不如预期的大（只有一万多人签名），发起人刘晓波的被判刑也是波澜不惊。反响不大的一个重要原因是，最近几年兴起的经验主义、保守主义思潮部分地抵消了理性主义、激进主义影响。宪章似乎就是理性主义――人类凭借既有知识和即时创能就可甩掉旧世界、开辟新天地――的产物。经验主义、保守主义是真正高深的知识分子对社会发展规律的正确理解。可惜，朦胧的多数人（也包括相当一些知识分子）尚未认识到这一点。<br />
<br />
四，相比上述朦胧的人，中共却似乎高人一筹。早几年由其发起的对以儒家为主的传统文化的尊崇，是否说明中共已经或正在抛弃其起家时信奉的理性激进主义？我倾向于相信这一点。正因如此，中共可以毫不留情地从教科书中删除曾经被他们顶礼膜拜（当然你也可说利用）的鲁迅――激进主义的代表。正因如此，中共自认已经找到了正确发展的哲学理念。正因如此，胡锦涛才在60大庆上理直气壮地许诺“两个百年”时的壮丽前景。正因如此，此次两会的新闻报道才有意露出狐狸尾巴给人看。这不是中共的自信是什么？<br />
<br />
最后想说的一点是，我毫不怀疑人们追求民主的良好愿望。但，知识分子必须首先重新梳理清楚和深刻理解各种主义，包括我们曾经批判的主义。就我个人的认识而言，我认为，个人主义，功利主义（承认利益驱动），市场主义（我发明的，呵呵），经验主义（智慧主要来源于此），保守主义（尊重传统、主张渐进），自由主义（消极的），宪政主义（约束公权）都是比较好的主义。反共却依旧沿用中共曾经信奉如今已被其抛弃的主义（比如激进主义），则很难成事，成了，也可能是又一场灾难。<br />
<br />
当然，如今中共的理念虽有很大进步，但也有不如我意的地方（一笑），比如对市场的干预，迷信人为的二次分配可比自然的市场分配更公平，法律的制定和更改上不尊重经验（比如对小产权房的不承认），等等。这说明，理性主义的幽灵还在他们脑中徘徊甚至重新泛滥。不过，既然我信奉上述自列的一系列主义，那么，我就依然主张渐进，给它时间吧。<br />
