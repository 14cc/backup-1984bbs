													<h2>张悟本小虾米耳 西药厂才是巨贼</h2>
						
						
																											<div id="postmessage_677773" class="t_msgfont">昨日跟老板同坐一张饭桌上吃饭。烧饭婆上来一盆香菜炒花生米，大伙儿瞪着双眼挥动筷子疯狂地将香菜往嘴里送，等我反应过来香菜就没了，于是我便酸溜溜地说：“香菜吃多了，杀精啊！”。同事小C嘿嘿笑着说：“他妈的就你会养生”。老板听了嘴角流出一丝诡秘地微笑：“小沙啊，你不会以后也成了第二个张悟本吧”，我立刻抓狂。<br />
&nbsp; &nbsp; 张悟本成为过街老鼠是必然的，早在年初网络盛传他的养生视频时我看了就断定这小子根本不懂中医学的基本理论，比如他宣称“人体永远是热的，即使人怕冷也是因为他身体太热了，因此通常需要寒凉之物来降热泻火”。这种观点是金元以降医道沦丧之后形成的温病派谬论，以为不管什么病只要投以寒凉泻火之品就能定起沉疴。殊不知人体之所有得病十有八九是“阳不足而阴有余”造成的。因为根据自然规律人年纪越大阳气越衰，阳退一寸而阴进一寸，直至最后真阳耗绝才沦为纯阴之鬼。像张悟本这种半路出家的养生专家最多读了几本《饮膳正要》、《汤头歌》等通俗读物，也加杂点西医常识以此来论述中医。根本不可能读懂甚至读过《伤寒论》、《医宗金鉴》、《中西医汇通》、《难经》、《神农本草经》等传世经典。从他的五行汤配比来看简直是狗屁不通牵强附会，完全没有领会医圣的真意。<br />
&nbsp; &nbsp; 但是不管怎么讲，他劝癌症病人和高血压病人停药是对的，这是他对底层群众最大的贡献。为什么这么说呢？因为大部分癌症病人和高血压病人不是发病死的，而是把西药当饭吃吃死的。像癌症病人，真阳弱如游丝，身体防疫系统濒临崩溃，只靠一点胃气来维持五脏六腑功能的正常运行。只要病人有饥饿感，只要病人大小便正常，加以心理治疗他们活个五年八年甚至更长都没问题的。但是西药这种偏酸性物质加速了胃功能的恶化，摧毁了人的意志，使得病人花光所有积蓄只买到了两个月三个月的寿命，这不是很冤吗？<br />
&nbsp; &nbsp; 还有高血压，大部分根源在于肝脏和血管上。如果投以疏肝活血之品冲刷血管这条河道，使得血管壁的淤积随着人体代谢排出去，高血压就搞定了。而治高血压的西药只知道将人的血液变粘稠以减慢血液的流速使得血压降低。但这只是暂时的，血液流速慢了，血液中的残渣物便在血管壁上继续沉淀，就好比河床又抬高了，血压自然又升高。这就是为什么越吃西药血压越高的道理。<br />
&nbsp; &nbsp; 张悟本的学历作假自有相关部门处理，但是有关媒体总是揪住他的下岗工人身份不放我就觉得有点变态了。下岗工人怎么了？就不能自学成才吗？即使他学艺不精也懂得拾人牙慧。医圣张仲景行政干部出身，也不是成了一代宗师吗？清代医圣傅青主是武林高手、诗画一绝没读过一天医学院，不也成为一代妇科名手吗？大部分名中医都是靠自学成才的，因为学中医是聪明人大丈夫所为的事业，智商低的人根本学不好。像张悟本、中里巴人、曲黎敏之类低智商者也只能靠敲背按摩、贩卖食疗混成大师。反观现行国内医学院毕业的中医学硕士博士们，大部分是废物。我认识一个中医硕士，连古文也看不懂，《伤寒论》也没看过，据说他读书的时候《伤寒论》是作选修课来学的。我再问那你平时都看些什么经典？他说最近正在看《求医不如求己》！我直接崩溃，中医硕士就这种水准，放着理论典籍不看却看这些东拼西凑的地摊小书。真是中医学的掘墓人啊！<br />
&nbsp; &nbsp; 但相比较张悟本这种只会推销绿豆茄子的江湖小贩、末流中医，西药厂的老板们及医药监管部门们更可恨，他们才是真正的国之巨贼。他们偷换概念、掩盖真相、抬高药价使得神州大地陷入抗生素的海洋、毒药的泥沼。明明可以物理治疗的轻微感冒他们都要骗你花几百上千挂吊针，非得要将你往死里整，尤其是将小朋友往死里整。<br />
&nbsp; &nbsp; 相比较张悟本这种只会泻火的低级玩家，西药厂的老板们可是直接让你肾衰竭、胃气死绝。<br />
&nbsp; &nbsp; 相比较张悟本这种收2000挂号费的小贪，西药厂几毛钱的毒药都要卖你几百上千每年获利都是几亿甚至几十亿上百亿。<br />
&nbsp; &nbsp; 谁才是大骗子？谁才将我等屁民的性命视为玩物？<br />
