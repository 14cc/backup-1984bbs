																											<div id="postmessage_440647" class="t_msgfont">王力雄：普通的法西斯<br />
<br />
很多在自由社会生活的人不理解中国人表达民族主义时那种对专制政权的认同，难道中国人有不同的人性，真的喜欢在专制统治下生活吗？我认为不妨从几个方面看：<br />
<br />
一是中国民众对政权的认可，大多来自这几十年的经济发展，虽然社会存在种种矛盾，只要经济保持高速增长，按照中国俗话说就是“一俊遮百丑”。不过以常识也可以知道世上没有永远高速增长的经济。一旦有一天经济出现问题，原本被掩盖的社会危机和政治危机就会随之浮出，那时对政权也就会转变为不满。<br />
<br />
二是目前外界能看到的中国人基本是居住在城市、受过较好教育的人。他们的声音虽大，在人口中的比例并不大。对中国的广大人民――底层百姓，工人、农民、农民工、上访人而言，民族主义远远不如他们切身的公正、人权、自由和民主更重要，只是他们无从发出声音，是沉默的大多数。<br />
<br />
三是极权主义擅长运动群众。面对宏大问题，民众因为没有足够信息与知识，容易被煽动和操纵。德国曾经有一个中学老师在班级里做实验，只用了5天时间，就把在民主社会长大的孩子变成了具有法西斯性质的群体。那么，中国以整个国家机器进行了60年灌输，民众形成目前这种民族主义思潮不奇怪。<br />
<br />
相比更糟的是专制制度培养出一种人，被称为“普通的法西斯”或“平庸的法西斯”。他们并非专制权力的当权者，只是专制机器上的细小零件。他们并非没有自己的思想，但是正义、良知、真理不会成为他们的行事准则，他们的心目中只有个人利益，把服从和执行专制的指令当作谋生的职业，对自己行为进行开脱的理由就是“要吃饭”，心安理得为专制权力充当工具，去从事政治迫害等行为。这种“普通的法西斯”，汉人中有，藏人中也有。<br />
<br />
“普通的法西斯”平时看上去是善良市民、慈爱父母或是和气的邻居，然而正是这些小人物，保持了专制机器的运转，维持了专制制度的稳定。如果没有他们，专制政权一天也存在不下去。当一个民族长期被专制政权统治时，正是因为有培养和生长这种“普通法西斯”的土壤。而让人不乐观的是，中国社会的这种土壤十分丰厚。<br />
<br />
