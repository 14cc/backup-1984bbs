													<h2>把人教育成狗的儒家“文化”</h2>
						
						
																											<div id="postmessage_458946" class="t_msgfont">什么是文化？文化是人类生活的反映，活动的记录，历史的积沉，是人们对生活的需要和要求、理想和愿望，是人们的高级精神生活。是人们认识自然，思考自己，是人精神得以承托的框架。文化的最基本作用，就是表现人们最基本的对事物的认识。如“何为物质”，“什么是社会”，“智慧是什么”及最基本的人类伦理“真.善.美”。<br />
其中，占据文化最核心部分的，无疑是人类最基本的伦理“真善美”。对“真善美”的判断，是人类一切美好文化的基础。对“真善美”的共识，是社会秩序产生的必要条件。也是一切事物或论点的是非得失的根据――道理。<br />
然而，我们看到，所谓的“儒家文化”中，并没有包含对真理的追求和对道理的认识。事实上，整个“儒家文化”的基础是构建在“伦理”这个假冒伪劣的“道理”之上的。儒家的“理”，无论是孔子的“礼”，还是朱熹的“天理”，其实质仅仅是有关人的社会行为的一种变相强制的“规则”，”仪式“。而不存在真正的“道理”，既对“真善美”的追求。所以，”儒家伦理“的提法应该被废除，因为儒家无理，只有”礼“。很多善良的人们，就这样被儒家玩弄的一点小文字游戏所欺骗，误以为儒家是一门具有积极意义的”文化“。<br />
事实上，儒家”文化“的核心――伦理的本质不过是”伦礼“。换言之，儒家根本就不能称之为文化，也就是说，长期接受“儒家文化”熏陶，“儒家文化”深深的铭刻在我们的民族性之上的中国，正如西方学者所判断的那样，是一个“没文化的国家”。而儒家重“礼”，轻“理”的核心理念，又对中国人的社会民族性带来了极其恶劣的影响。<br />
儒家“文化”，从创立以来就不重视人与人交往和社会发展的基础“真善美”，而是用对“贵贱有序”的等级制度的追求，取代了对真理的追求。所以，自“独尊儒术”之后长期以来接受儒家教育的中国人，内心永远都是只有“尊卑”，没有“是非”，这种”以尊为真“的思想体系造就得对”尊“的”主人“&quot;尽忠”的行为模式，我们在一种对主人无比”忠诚谄媚“的动物――狗身上也能发现，所以，中国人的“文化”实质上是一种狗的行为模式，中国人在儒家的教育下，一代代的向“狗”发展，“下跪文化”，“奴才文化”，“太监文化”的盛行，和以”为尊者讳“思想为基础，扭曲事实，颠倒是非，为邪恶的主子和罪恶的儒家思想辩护的丑行，竟然能在这么个号称&quot;儒教已死”的时代，出现在某些高喊”追求自由民主“口号的民主人士如杨恒，风餐露宿的身上。<br />
一位先贤说的好！儒家“文化”，“用“亲”取代了“真”，用“贵”取代了“善”，用“尊”取代了“美”。”因为儒家只重“君君臣臣父父子子”的礼教，轻视对真理，对善良，对美好事物的追求。在这样的“文化&quot;熏陶下，中国人除了变成狗一般”愚忠“，是非不分，唯主命是从，不是对错善恶，只知贵贱亲疏。这种”人“，是真正意义上的人么？这种没有真正意义上对是非，对错，美丑的追求的伦理的”文化“，是真正意义上的文化么？<br />
为什么中国长期在封建君主专制的泥淖里挣扎？为什么中国人都是”愚民',&quot;贱民“，偶尔出一两个对现实不满的清醒人，还被扣上”刁民“的帽子大加鞭笞？我认为，正是由于儒家的所谓“文化”的“伦理”，也即儒家伦礼。给中国人的脖子上套上了条无形的狗链子，使中国人失去了对真理，是非，美丑的追求，使中国人永远都只能辨别、判断“亲疏”、“贵贱”、“尊卑”，像狗一般为尊者命是从，不问对错，只知维护”尊主“。并让他们之中的大多数全都只能听从“天命”的安排、听从“大人”的奴役、听从“圣人之言”的训导，从而安安心心地“修己安人”、安安心心地吃苦耐劳、安安心心地服从自己狗一般卑贱的命运、安安心心地当奴隶、作牛马、做一条忠实于主人的狗。而我们的历史，也早已告诉我们，在这样的文化下，中国人过的多么悲惨，多么愚昧。为什么中国人在共产党的暴政下依然安之若素不知反抗只知下跪，口口声声”自由民主“的杨恒式”民主儒徒“是思想上依然没有摘下愚昧的狗链而不知道反省过失，与邪恶儒教决裂，还是当狗当惯了，不愿意摘下脖子上的狗链子，追求真正的自由民主呢？<br />
不客气的说，所有主张恢复继承儒家传统”伦理“，”文化“的人，无论他表面上是什么派别，他的内心，依然是一些带着”伦礼“狗链的狗行人，一些以做狗为荣，做人为耻的中国制造人形垃圾，以做狗为荣的狗奴才。或者是共产党，刘松萝型的”圣人“，”贵人“们，为了继续享受”君君臣臣父父子子“的儒家”伦礼“观带给他们的既得利益而继续鼓吹儒家”文化“来蒙骗我们的子孙后代继续做狗，为他们服务。事实上，前一种人最为可恨，他们以“亲疏”、“贵贱”、“尊卑”的“儒家伦礼”的狗眼看世界，甘当卑贱的狗奴才，永远把自己看做是一条狗（或许这就是为什么中国要出台法律来保护狗――认同儒家”文化“的中国人同类的原因吧）。这些人不但自己要永远当狗，更试图把手伸到教育――甚至是”政教合一“，要让我们的子孙后代都成为和他们一样的儒家两脚狗!<br />
这种把我们和我们的后代教育成”狗“的”文化“，”伦理“。觉醒的中国人能允许它在毒害完我们的祖先，我们的伙伴后，继续残害我们的子孙后代吗！为了中华民族的未来，为了我们的子孙后代！觉醒的中国人，起来！杀光儒教的人型狗们，让儒教变成第二个拜火邪教！为了我们的子孙的做人权利，把毒害我们中国人两千多年，罪恶滔天的儒家”文化“彻底埋葬！<br />
