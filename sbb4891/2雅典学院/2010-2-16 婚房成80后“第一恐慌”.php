																											<div id="postmessage_482055" class="t_msgfont">“80后”生存现状大调查：婚房成“第一恐慌”<br />
<br />
2010年02月15日09:04<br />
　　　五成“80后”自认压力大于“70后” 三成是“月光族” 四成“80后”每月补贴父母家用<br />
<br />
　　“80后”，目前中国最受关注的一代，他们代表了中国的未来。<br />
<br />
　　“80后”作为一个社会人口学概念，指出生在20世纪80年代的人，特指国家依法实行计划生育后所出生的一代人。据统计，中国的这个群体超过2 亿人。<br />
<br />
　　转型社会的洪流造就了“80后”的独特性，这些在温室里成长的“小皇帝”们，却要面对社会变迁进程中的狂风暴雨。头上顶着“垮掉的一代”、“叛逆”、“自私”、“脆弱”等负面评价，还伴生着“富二代”、“房奴”、“卡奴”、“蚁族”、“宅男”等社会现象。<br />
<br />
　　为了真实反映“80后”的生存状态，日前，广州日报联合新浪网、大洋网，以及现代国际市场研究有限公司推出“80后”生存状态大调查。截至2010年2月5日零时零分，共有来自广东 、北京、上海、浙江等全国各地的3313位“80后”在网上参与了调查，为我们全面、客观了解这一群体的生存状态，提供了依据。<br />
<br />
　　调查发现，住房、婚恋、竞争压力、父母赡养等，构成了“80后”青年普遍面临的现实枷锁。理想与现实之间的落差让他们感到迷茫与焦虑。但他们同样相信，“风雨之后总会有彩虹”。<br />
<br />
　　“在奋斗的路上”――这是这一代人目前最重要的人生主题。<br />
<br />
　　“在这个国家，年轻叛逆者的数目正在如此迅速地扩张，就像美国垮掉的一代和嬉皮。他们已经有了他们自己的名称：另类。这个词曾经是贬义的，意指品格低劣的流氓。而在今年最新修订的《新华词典》――中国最权威的词典中――对‘另类’的解释则是‘一种特别的生活方式’，不再有贬低的含义。”《时代周刊》如此写道。<br />
<br />
　　这是《时代周刊》亚洲版2004年2月2日的报道，这篇名为《新激进分子》的报道也许是海外媒体第一次对中国“80后”一代的聚焦。<br />
<br />
　　时至今日，“另类”早已不足以概括中国的“80后”。本报调查发现，对于社会舆论贴在自己身上的“宅男宅女”、“脆弱”、“自私”、“垮掉的一代”、“叛逆”等标签，大部分受访者并不十分认同。33.2%的受访者认为这是社会加诸于“80后”身上的不公正的标签。<br />
<br />
　　梦想很遥远，现实很残酷。为了探清“80后”们真实的生存现状，广州日报日前联合新浪网、大洋网，以及现代国际市场研究有限公司展开了一次大调查。<br />
<br />
　　压力：<br />
<br />
　　“80后”幸福感普遍不强 过半自认压力大于“70后”<br />
<br />
　　在许多人看来，“80后”是温室里长大的幸福一代，但调查显示，他们的幸福感却普遍不强。<br />
<br />
　　在回答“你觉得‘80后’面对的压力大吗”的问题时，52.6%的受访者认为“很大，超过‘70后’、‘90后’”；37.0%对此持宽容态度，认为“每一代人都有自己的压力，这是正常的”；8.7%认为“一般，不如‘70后’和父辈”；只有1.7%的受访者认为“压力很小，我们是幸福的一代”。<br />
<br />
　　那么，他们的压力主要来自哪里呢？48.1%的受访者认为压力主要来自“生活，如买房子、养孩子”等；29.7%受访者认为来自“工作，要闯出一番事业太难”；18.7%认为“整个社会都在给我压力，压得喘不过气来”；另有3.5%表示压力来自于家长对自己的期望过高。<br />
<br />
　　消费：<br />
　　仅占一成有私家车 六成表示两年内不买车<br />
<br />
　　相对于婚房这类迫切的刚性需求，车子对于“80后”而言还是奢侈品。<br />
　　油费、桥路费、停车费等养车成本居高不下，购车后的日常支出直线飙升，令大部分“80后”不敢拥有自己的座驾。调查显示，“80后”中的“有车一族”仅占13.5%，19.7%的人表示打算近两年内购买；而66.8%的受访者表示近2年内不会考虑买车。<br />
<br />
