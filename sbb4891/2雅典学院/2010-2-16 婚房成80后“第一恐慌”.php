<br />
　　调查显示，38.2%的“80后”每个月都会给父母补贴家用。其中，29.0%每月给1000元以下，6.9%给1000元~2000 元，2.3%给2000元以上。尽管不算太多，但对比于“81%月入6000元以下”的收入水平而言，每月补贴家用，仍体现了“80后”对父母的孝顺。<br />
<br />
　　与此同时，需要父母每月给钱过活的“啃老族”仅占4.8%。排除因读书深造而未获取经济收入等特殊因素，“80后” 并不像有些人臆测的那样是“啃老族”。<br />
<br />
　　工作：<br />
　　青鸟综合征 近七成不满意生活水平 近五成想立即跳槽<br />
<br />
　　调查显示，有54.5%的“80后”曾有过跳槽的经历。其中，有三次以上跳槽经历的，占23.7%。而46.8%的人“现在就有跳槽的打算”；从没有跳过槽的“80后”占45.4%；只有12.2%的人打算在现在的单位干到退休。<br />
<br />
　　这反映出“80后”群体普遍受到“青鸟综合征”的困扰，具体表现为：总是对自己现有的成就不满，频繁跳槽，找不到适合自己的职位。总认为“理想在别处”。就像童话《青鸟》中总也找不到理想的那只青鸟，只是一直不停地奔跑。<br />
<br />
　　调查还显示，有高达67.85%的受访者对自己的生活水平感到不满，考虑转换环境。但因为生活经济压力，敢于跳槽的仍属少数。<br />
<br />
　　收入：<br />
<br />
　　五成“80后”月收入3000元以下 三成是“月光族”<br />
<br />
　　调查显示，“80后”群体的收入状况在6000元附近开始呈现“二八分化”。81%的受访者表示月收入在6000元以下，其中月收入水平在 3000元以下的，占比高达52.6%。<br />
<br />
　　在19%自称月收入在6000元以上的高收入“80后”中，6000~10000元/月的占10.1%，10000~20000元/月的占 5.8%，20000元/月以上（含20000元）的占3.1%。<br />
<br />
　　值得注意的是，月收入10000~20000元、20000元/月以上的高收入群体相对集中于京沪粤三地。<br />
<br />
　　而在京沪粤等生活成本偏高的大中城市，月收入3000元以下，实在不值一提，刨掉交通、吃住、通讯等基本花销，几乎所剩无几。对于生活在这些城市的“80后”，生活压力相当明显。调查显示：29.1%的“80后”是“月光族”――每月收入基本当月花光的“零存款一族”。<br />
<br />
买房：<br />
<br />
　　八成“80后”存款不足10万元 婚房成为“第一恐慌”<br />
<br />
　　当被问及“你现在有多少存款”时，27.4%的受访者表示存款数目在1万~5万元之间，26.7%表示在1万元以下，14.2%表示在10万元以上，10.5%在5万~10万元之间。合计，存款不足10万元的“80后”超过八成（85.8%）。<br />
<br />
　　毋庸置疑，在如此的经济收入和存款状况下，大部分“80后”虽然对基本的生活花销可以“收支平衡，略有盈余”，但是对购房、购车这类大宗消费，便显得心有余而力不足了。以一套80平方米的一手商品房为例，均价在10000元/平方米以上，首付三成至少需要30万元，这对绝大部分存款不足10万元的工薪族“80后”而言，单靠自己的积蓄，支付购房首期简直就是天方夜谭。<br />
<br />
　　调查中，只有24.2%的“80后”表示“能自己一次性存款付清房钱”，21.6%表示“父母资助大部分，自己（含配偶）存款占小部分”； 17.1% 表示“父母资助小部分，自己（含配偶）存款占大部分”； 15.7%表示父母（含亲戚）全额资助 。至于买房的主要目的，近四成是为了结婚。<br />
<br />
　　生活：<br />
　　休闲时间习惯上网 恋爱及性的态度相当开放<br />
<br />
　　有57.8%的受访者表示，生活中最主要的休闲娱乐活动就是上网，上网，已成为他们的生活方式。<br />
　　相对于父辈，他们对恋爱和性的态度相当开放，对婚前性行为的认同度和接受程度更高。<br />
　　调查中，31%的“80后”表示是在工作后谈的恋爱，26.6%表示在上大学时候开始谈恋爱。在对第一次发生性行为的时间的调查上， 24.9%表示大学期间发生过性行为，18.0%表示至今没发生过。<br />
<br />
　　理想：<br />
　　有房有车有事业<br />
<br />
　　几乎每一个人在小学时都曾以“我的理想”为题写过作文。那么，“80后”的理想又呈现出怎样的特点呢？调查显示，“80后”的理想较为集中在四方面：一是有房有车，特别是买房；第二是事业有所成就；第三是做个有钱人，特别是当老板，也有想一夜暴富中五百万元彩票的；此外还有成为公务员、成为科学家和军人等“对社会有用的人”。<br />
