													<h2>韩寒:G8高速公路(2009-10-13 04:28:05)</h2>
						
						
																											<div id="postmessage_282709" class="t_msgfont">为了和国际接轨，在几年前和国际接轨过一次的上海又改高速公路名称了。在几年前的几年前，大家都知道沪杭高速，也许是某个领导出国考察了一次，觉得纯中文不洋气，也许某个外国人投诉他们看不懂，也许是某个大领导御驾亲征从杭州去上海的路上一时心血来潮，以他的座驾A8御赐了一个路名，总之，这条高速公路以后就叫A8了，但是下的决心很大，非常的彻底，大大的路牌只剩下A1,A2,A3,A4,A5,A6,A7,A8,一直到A30，连哪里到哪里都没有人知道，整个上海就是一个奥迪的展厅。<br />
<br />
就像一个人突然上前踹你一脚你可能记不住他的名字，但是如果它天天踹你你就记住了他一样，经过了多年的强记，愚民们刚刚记住，但是还是习惯要加上后缀，A8沪杭高速，A9沪青平高速，A20外环线……<br />
<br />
事情按理来说已经过去了，但是，北京突然执行了每周一个尾数限行，中国的交通部门就像教育部门一样，最喜欢考验大家的记忆力。我认为，这是一种政策的平衡，也就是说，北京人民每天要换算自己的车能不能出门，经常脑子抽筋，上海人民没有道理不受这个苦，但因为上海人民已经缴纳了牌照保护费，也不能不让人家上路，所以，新招出来了，在脑力上折磨你，原来的A8,A5都改成了G2,G啥来着，而且连后缀也索性改了，比如原来的A9沪青平变成了G50沪瑜高速，A30更是变成了G1501，A4莘奉金高速变成了G15沈海高速。<br />
<br />
这个世界突然都变了，以前去我家，是A8转A5转A30，现在是G2转G15转G1501，你很难相信自己还是生活在昨天的那颗星球上……好比看国庆庆典的时候，我们好不容易看图说话认清楚了谁是谁，但是中秋庆典，突然宣布中央领导人名字全部都换了，但是人还是同一个，大家一时当然难以接受。<br />
<br />
说实话，现在的标示也没有太不合理的地方，也号称是国家规范了，但问题不是合理不合理，比如我们的电话号码也很合理，但定期系统强制给你自动换一次号码，那就是不合理，哪怕是给你换了一个不带4的号码。因为这么一来，早先的那一次换路名算什么呢。如果说上一次是市政府行为，这一次是中央行为，那麻烦领导们以后还是要沟通好，因为我们既不能和市政府沟通，也不能和中央沟通，但是市政府和中央是可以经常勾结的――不好意思――沟通的。<br />
<br />
以上这些其实都不重要，也就是杀死一点脑细胞，多走几次冤枉路，但是从新闻晚报上，我们注意到，这次路牌的更换行动需要花费2亿元人民币，总计更换 5000块路牌。两亿当然是个大数字，市政府认为，两亿不过是阿拉两套好一点的房子的价格。两亿在我们眼里是钱，在仆人眼里基本就不是钱。这点钱，多倒钩倒钩，不就有了嘛。闵行不就光荣宣称，整治黑车短时间就整治出5000万的罚款嘛，加大整治力度不就有了嘛。<br />
<br />
但是这个也不重要，重要的是除一下，也就说，每块路牌的费用是4万。<br />
<br />
一块铁皮四万块，耳熟吗？是的，和你车上的那一块的价格是一样的。这说明了多年了，上海的车主其实是冤枉了上海市政府，你以为这是敲诈吗？不是的，这四万其实是原材料的费用。要不他们自己给自己的铁皮怎么也得收自己这么多钱呢？当然，还有一个可能性是中央拨款，让上海报价，上海说，我们这里的铁皮，无论大小，均价四万。除非你都把高速公路改成C15,C2什么的，C打头的牌照阿拉不收钱的。<br />
<br />
经过了这次2亿的改造，我建议我们的世博会口号改成――最贵的地皮，最贵的铁皮。</div>

							
							
							
							
