													<h2>韩寒：这是一个庞大而复杂的工程</h2>
						
						
																											<div id="postmessage_282704" class="t_msgfont">昨天，我们得知上海更换5000块路牌总共耗费两亿元人民币，这个数据是根据上海《新闻晚报》的报道得来。可以想到，相关部门和找到了《新闻晚报》说，你看，你们的报道，给党和政府的工作带来了麻烦，你们必须亡羊补牢，于是，《新闻晚报》今天刊登了文章进行了解释：这5000块指路牌只是一小部分，其实只占有所有更换量的五分之一。也就是总体要更换25000块各种路牌。所以，就用2亿除以5000来计算出40000一张路牌，“太过于草率”。<br />
<br />
在《南方都市报》上，公路管理处说，这是一个庞大而复杂的工程。<br />
<br />
那好吧，首先，这5000也是你们说的不是我意淫出来的，就算你说错了吧，是你把2留给了自己。那就更换25000块路牌。25000块路牌是个什么概念呢，25000就是250了100次，是一个大数字。据悉，上海的高速公路总里程是600公里左右，这25000块牌子中，假设有1000块做了高速公路附近公路引路牌的更换，那么还有24000块是用在了高速公路上，于是，我们再做一下除法（学会做除法很重要），我们发现，上海的高速公路每公里需要更换的路牌是40块，也就是说，你在上海的高速公路上开车，每开25米，你就能看到一块牌子，假设你的速度是120公里每小时，也就是说，你每秒钟行进了 33.333333333米，这代表着，你在上海的高速公路上开车，两秒钟内你差不多能看见三块路牌或指示牌。<br />
<br />
太狠了，如果我开的足够快，上海市公路管理处在路牌上画点图，我就能看动画片了。<br />
<br />
假设我们公路公里不止600公里，用在高速上的各种牌子也没有那么多快，两边都朝相关部门的有利方向放宽尺度，那我们一秒种也能看见一块牌子，晚上这些牌子还都反正光，一分钟你看六十张牌，请问上海的驾驶员们，你们开在高速公路上的时候，有过这么梦幻到晕菜的时刻吗？<br />
<br />
所以，我认为，相关部门的解释未免太过于草率了，你撒谎好歹也要撒在人体工程学以内啊，你这是换路牌，你当贴瓷砖啊。<br />
<br />
我们再退一步，假设他的确大大小小是改动了25000块牌子，连相关部门领导自己家的门牌都改了，花费两亿，那每块牌子的平均价格也达到了8000元。8000块一块牌子，这个工程我很愿意承包。<br />
<br />
所以，这两亿里有没有猫腻，其实很简单，公路管理处把账目公开了就行了嘛，包括你把这个工程承包给谁了，那事情就好说了嘛。你心中无鬼，我们又过于草率，那为何不给自己一个清白呢？<br />
<br />
当然，相关部门的说辞永远是很多的，他会说，我们给司机印发了免费的地图，印了一亿份，这钱就花完了。这下就死结了因为你无法查证了。因为出版社和印刷厂隐瞒印数是我们国家文化产业的一个特色。<br />
<br />
最大问题正是反映出我们国家爱折腾的特点，中国的道路交通中，我听到的最多抱怨就是路况差，乱收费，道路管理混乱，设计不合理，施工质量差等等，很少听到有人反应说路牌不规范。比如上海的高速公路，其实大家已经很熟悉，原先的A4，是莘奉金高速，就是从莘庄到奉贤到金山的高速公路，一目了然，现在变成了沈海高速，就是沈阳到海口的高速，公路管理处的专家解释说，这样“方便驾驶员认准编号，一路到底”。<br />
<br />
你们觉得驾驶员同志都是有毛病的吗？你没事上高速公路就一路到底？是为了方便那些本来开到松江的同志开到松花江吗？这样的理由是非常可笑的，你索性就说，国家在下很大的一盘棋，观棋不语。那就可以了。但是，换一下高速公路路网的路牌，这个国家就花费了几十亿上百亿，那这个国家花钱是不是也太草率了？我们的教育，福利，扶贫为何又要常常哭穷呢？以前换下来的那些路牌怎么处理呢？万一又要换一次呢？换路牌是眼前的燃眉之急迫切需要解决吗？<br />
<br />
另外，原本我家门口有一条国道，叫G320，本来G就是国道的标志，现在高速公路也是G打头了，那如何从地图上区分国道和高速呢？<br />
<br />
再另外，相关部门说，他们用两年的时间做了大量的调查研究和广泛征询意见，我周围的朋友很多开车，包括所有的汽车论坛上，从来没有看见过有人是被征询意见的。我认为，征询了老婆情人二奶的意见不能算是广泛征询意见。相信这只是相关部门自己决定以后的随口一说，就像那100个250一样，都是他们的惯性谎言。<br />
<br />
其实，我认为，高速公路的标示用中文也挺好，高1高2高3，国道就叫大1大2大3，省道就叫初1初2初3，终点都是天安门，广场上竖一个大路牌，毕业。然后索性多花点钱，把所有的路牌都换成电子大屏幕，可谓一劳永逸，从国库的角度来说，其实是节省的，你不光可以按照心情随时更换路牌路名，也可以经常进行一些政策的宣传，放一些领导的头像或者通缉犯的头像，总之，电子大屏幕是很方便操作的，也正符合我们政府随性洒脱的决策风格。<br />
