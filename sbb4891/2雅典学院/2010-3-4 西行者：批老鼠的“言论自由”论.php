<br />
《副总统干啥用的？》<br />
<br />
<a href="http://coninex.com/wordpress/2009/12/%e5%89%af%e6%80%bb%e7%bb%9f%e5%b9%b2%e5%95%a5%e7%94%a8%e7%9a%84%ef%bc%9f/" target="_blank">http://coninex.com/wordpress/200 ... %e7%9a%84%ef%bc%9f/</a><br />
<br />
11 December 2009 No Comment<br />
根据宪法，副总统的职责只是在总统辞职，死亡，出现意外，不具备行为能力的时候接任总统的位置，还有出席参议院投票在出现50：50的时候投下决定票，仅此而已。但实际上来说，副总统的作用和总统一样也在不断扩大着。<br />
<br />
首先就是大选时期的作用。副总统在大选时期都是总统候选人的running mate，选择副总统候选人本身就是一个很具有挑战性的工作。其实从美国建国之日起，副总统候选人基本的作用就是弥补总统候选人的不足以及软肋。<br />
<br />
最 基本的考虑就是地域平衡的考量。比如说华盛顿的副总统亚当斯来自马塞诸塞州，华盛顿来自维吉尼亚州。这两个州当时是美国精英最集中的两个大州，维吉尼亚在 建国初期出了好几个总统，日后的首都也选在这里，而马塞诸塞州则是哈佛大学的故乡，清教徒的大本营。可以说华盛顿选择亚当斯的目的之一就是为了平衡南北问 题，争取北方对联邦的支持认同。而之后的亚当斯-杰斐逊组合，杰斐逊-伯尔组合基本上都是遵照南北平衡的目的拉郎配的。再往近点，比如肯尼迪-约翰逊组 合，要不是约翰逊肯尼迪恐怕那届大选根本都不过尼克松，德州也不会被民主党攻陷。还有里根-老布什组合，则是东西配，一个加州的一个康州的。<br />
<br />
还 有就是经验的补充，这个最典型的就是最近两任总统了。布什当年也是菜鸟，虽然在党内初选期间摧城拔寨，但是大多数人都很怀疑布什的治国能力。结果布什选的 就是老谋深算的切尼，这个不论反对派还是支持派都承认的”解决问题的能手”，一下子就让党内支持者打消顾虑。当然布什起用的也不光是切尼一个，鲍威尔的入 阁也是布什的加分点。奥巴马就更嫩了，如果说布什好歹还在德州做了几年州长，奥巴马则是从政资历都短的可怜。所以这也成为希拉里麦凯恩大肆批评的重点，所 以奥巴马在选择副总统候选人的时候着重考虑的就是需要一个有资历的人辅佐。你麦凯恩不是在参议院干了20多年么？我找一个更久的，结果找到了现在的拜登， 从70年代就开始在参议院混的老油条，彻底堵了质疑者的嘴。当然也有反面的例子，比如2004年大选的时候切尼就跟支持者说，假设克里突然挂了，你们信得 过爱德华这样的嫩鸟接任总统做我们的三军统帅么？这家伙连枪都不敢摸的。（爱德华是克里的副总统候选人，反对宪法第二修正案）最后大选结果民主党连爱德华 老窝的北卡都没能拿下。顺带一提，克里选爱德华的目的也是为了南北，克里麻省的，爱德华北卡的。<br />
<br />
在后面就是意识形态的补充。小布什第一次大 选的时候很多福音派极右派对这个名不见经传的家伙根本不了解，也不晓得他是不是反对堕胎支持携枪等保守议题的铁杆，所以布什为了稳定军心就拉了切尼，这个 绝对的极端保守派一下子就让共和党团结起来了。麦凯恩选佩林目的也在于此，他自己是共和党中间派，在很多议题上甚至和民主党走的很近，比如2006年的移 民改革法案就是他和民主党大佬爱德华肯尼迪提出的。因此他的基本盘非常不稳，很多保守派人士都纷纷表态不会去投票。所以麦凯恩为了安抚右翼不得不选一个立 场极端的搭档，最后就是这个迷信驱魔的莎拉佩林。<br />
<br />
还有一个就是选举议题的加分，比较典型的就是克林顿-戈尔这对年轻组合。这对组合非常颠覆 传统，两个人都是南方州出来的，年纪相仿，政见接近。当时非常不被看好，但最后还是希拉里亲自操刀的这个组合笑到了最后。戈尔在竞选期间非常有战斗力，独 自杠上了老布什政府的诸多问题，在全球暖化外交策略等诸多领域展开较量，堪称一条好狗。而这些议题都是当时非常尖锐的问题，克林顿对这些问题涉猎不深所以 就让戈尔全面担纲。之前不久戈尔的儿子又死了，戈尔的表现让很多人认同他对家庭价值的理念，无形中给深陷婚姻丑闻的克林顿拉了不少票。<br />
<br />
最 后就是对竞争对手的反击。麦凯恩为什么选佩林？主要就是针对奥巴马的软肋来的。第一，奥巴马没有执政经验，但是他自己也没有，而奥巴马的搭档还是没有，所 以这时候选一个当过州长的佩林出来很快就脱颖而出了；第二，奥巴马刚灭了希拉里，对女权主义打击很大，所以麦凯恩希望搞一个女搭档来分流那些原本属于希拉 里的票；第三，奥巴马不是鼓吹要变革华盛顿搞什么清新形象么，我就索性找一个离华盛顿十万八千里的北极圈来的，还不比你新鲜些？；第四，奥巴马打年轻牌， 麦凯恩年纪一大把了，那他也搞一个年轻的搭档来个老牛吃嫩草。可以说麦凯恩这个选择理论上来说是招招对准奥巴马死穴，可惜佩林是烂泥扶不上墙的阿斗，一张 嘴就漏底的主，没多久就把竞选策略搞砸了。<br />
<br />
副总统原来是很没劲的角色，因为是储君么，就像太子 一样。参议院什么50：50的机会也是千年等一回，所以愿意坐这个位子的，大多都是想利用跟着总 统抛头露面的机会增加曝光度，为以后自己媳妇熬成婆做准备的。而且成功的例子不少，比如亚当斯，杰斐逊，尼克松，老布什等等；还有就是美国总统被暗杀的也 不少，所以副总统接班的概率也不低。最牛逼的估计是福特了，接替了辞职的尼克松，竞选连任没有成功，结果成了历史上唯一一个非民选的总统。<br />
<br />
但是随着总统职能的扩大，副总统也不能闲着了。总统们也乐意让副总统担任一些艰巨任务，基本上最近20年都延续了副总统领头特别工作组的惯例，比如 克林顿时期的戈尔担任NPR工作组（national performance review，抓政府工作绩效的），小布什时期的切尼担纲的能源政策工作组（弄出安能事件，能耐不小），还有现在奥巴马手下的拜恩主管的中产阶级复兴工作 组（以观后效）。<br />
<br />
还有就是副总统的决策参与职能的扩大。说起来奇怪，副总统原来基本是闲职，没啥实权，很多事情都还没有内阁部长们实权大。但是渐渐的副总统也开始给 总统出谋划策了，担负起师爷的角色。比如里根时期的头几年老布什的权力大的吓人，总统有啥他有啥，给总统的文件材料他也会有一份。加上他好朋友担任白宫幕 僚长，里根那几年搞不好还是傀儡。戈尔的权力也很大，NPR干啥的？就跟我们纪委一样，下面的人不怕？看到他都要两股战战。戈尔推动互联网也是很积极的， 信息高速公路就是他发起的，没有他我们现在坐哪里都不知道。切尼就更不用说了，出兵阿富汗伊拉克否定京都议定书退出反弹道导弹条约都有切尼的影子，号称历 史上权力最大的副总统。拜登么，暂时看不出来，但这个人绝对是老奸巨猾型的，和浪漫理想主义的奥巴马截然不同。举个例子，2004年民主党给克里找搭档， 拜恩就提出要找麦凯恩打造两党结盟的组合，要不是麦凯恩放不下总统梦，布什2004年估计遇到的挑战会大很多。从这个角度看，他就不是一个常理思考型的， 谁会想到去敌人哪里挖墙角啊。这次大法官提名布局估计奥巴马也要借力拜恩，因为参议院司法委员会的前主席就是他，对那些繁文缛节的规章制度的了解简直到了 精熟的地步，要给新大法官保驾护航少不了他在背后牵线搭桥。<br />
<br />
副总统都大到这份上了，总统该有多大了？<br />
