会就表现得完全南辕北辙。这就是我前面所提到的许多外交纠纷的<br />
起因。<br />
　　顺便提一下，作为美国政府司法分支的法院，又相当于什么样<br />
的角色呢？我印象最深的是这个分支中的最高法院，他们好象有点<br />
象“婆婆”。他们平时很少抛头露面。但是，一旦出现什么争执不<br />
休的立法问题，他们会出来给个“说法”。一旦他们出来宣布某项<br />
立法“违宪”，那么，立马作废，毫无二话。<br />
　　当然，这只是我十分形象化的描述。美国政府的三个分支实际<br />
上还有更严密的相互制约的机制，以避免单纯的一层高于一层的简<br />
单构架，因为对于美国人来讲，高高在上的权力总是非常危险的。<br />
正是为了避免某一个分支爬上权力的顶端，所以美国政府的三个分<br />
支是相对独立的，互相之间始终存在着作用和反作用。也就是花了<br />
大力气，硬是把原来可以建成个宝塔的三大块给拖倒了，拉开后齐<br />
齐地放在平地上。这些，我想留在以后再慢慢向你介绍。 <br />
　　你看，我真是费了好大的劲儿，才刚刚大致搞清楚“美国总统<br />
是什么”这样一个看上去十分简单的问题。一度我曾觉得颇为窝囊，<br />
想不通一个简单的英汉翻译问题怎么会就把大家引向一片云山雾<br />
沼。后来，我觉得，这也许也是必然的。一个小小的翻译问题，实<br />
际上反映了两大文化背景之间深深的鸿沟。<br />
　　东西方各个历史久远的国家，尽管走的道路各不相同，可是它<br />
们毕竟走过不少相似的历史路径。它们在最初遭遇时虽然彼此陌生，<br />
但是，那些相似的地方又使它们产生似曾相识的微微惊喜。至少，<br />
你有一个皇上，我也有一个皇上，你的皇上管着一大群百姓，我这<br />
儿的皇上也管着一大群百姓。它们之间战也罢，和也罢，做买卖也<br />
罢，它们有文化沟通上的困难，至少不那么完全“隔路”。尽管此<br />
后它们各自都发生了许多变化，但是这种初次遭遇的经验仍然十分<br />
重要，它起码使得双方今后的相互了解，有一个渐进的过程。<br />
　　而美国却比较特殊。美国是一个历史非常短的崭新的国家。在<br />
建国的时候，它相对来说比其他任何国家的历史包袱都更轻一些。<br />
但是，它自行其是所搞的一套，也就更难被一些历史悠久而又文化<br />
背景截然不同的国家所理解。<br />
　　美国建国只有二百多年。我有时想，在二百年前，一个如初生<br />
牛犊一样的大洋彼岸的新国家，行事风格面貌作派样样都很“摩登”，<br />
一下子撞上一个历史悠久的东方大帝国，这大概是世界上最滑稽的<br />
事情之一了。<br />
　　美国是刚刚从一块英国殖民地独立出来的，皇帝皇朝一类的东<br />
西当然见得很多，但是，对方内涵截然不同的深厚文化积淀，肯定<br />
使这个本来一提传统文化就气短的新国家，久久摸不着头脑。<br />
　　从二百年前的清朝政府那一头来说，英国法国等等的洋人也见<br />
得多了，那美国佬还不是一回事。当时，听说那头没有皇上而只有<br />
总统的时候，这大清国上上下下，准是觉得这个叫美国的地界，是<br />
出了个什么新花样，楞要把他们的皇上叫作总统。除此之外，你说 还<br />
能有什么别的理解吗？<br />
　　可是，毛病很快就出来了。那头的总统常常说了不算，还经常<br />
狡辩说是他作不了主。你想想，我这个当皇上的能作得了主，以前<br />
打交道的那些英国法国的皇上他们也作得了主，你这个叫作总统的<br />
皇上却说你作不了主，谁信呀！<br />
　　两国误解的种子，一开始就自然地种下了。从英译汉的工作一<br />
开始，就不仅仅是一个文字工作，而是文化的对应和比照。在美国<br />
总统的后面跟的那个词，如果不是“政府”，难道还可能跟出什么<br />
其他的东西来吗？尽管，有大量的证据证明，这个词只是意味着一<br />
个“行政分支”，“执行机构”，可是，说是他们的皇上只管一个<br />
“行政机构”？这可能吗？<br />
　　我们可以想象，最初的翻译者和词典编写者，是何等痛苦地挣<br />
扎在两种风马牛不相及的文化思路之间。<br />
　　挣扎的结果，是他们彻底地放弃了自信，放弃了他们所收集的<br />
一大堆现成的资料。因为他们自己就是这个根深叶茂，郁郁苍苍的<br />
文化大树上结的一个果子。还能揪着自己的头发，把自己从这棵大<br />
树上摘下来不成？<br />
　　那么，怎么解决这个翻译上的问题呢？最后只能作一个变通的<br />
技术处理，就是把这个“政府的行政机构”改为“政府”。由于他<br />
们和我一样，也早早就发现一个事实，就是这个“政府”一词，从<br />
来就只是死死地跟在“总统”一词的后面。所以，他们又加了一个<br />
诠释。结果，我们说看到的这个词的注解：“（总统制国家的）政<br />
府”，也就这么顺应逻辑地出来了。这一下，大家的心里都踏实了。<br />
在自己的逻辑系统里，终于达到圆满了。<br />
　　这个小小的翻译就这样以讹传讹地代代相传，“克林顿政府”<br />
一词就堂而皇之地出现了，而且还出现在包括美国本土在内的中文<br />
报纸上。看报纸的人也都习惯了。也许，在深层次里，与这种文字<br />
所相连系的古老的文化逻辑，还在悄悄地起着作用。然而，今天，<br />
如果你想了解，进而理解美国政治制度和权力结构运作的话，你首<br />
先要记住，我们所读到的所有“克林顿政府”的地方都应该解读作<br />
“克林顿行政分支”，在读到“美国政府”的地方，你必须确证，<br />
那是指包括了立法，行政和司法三大分支的联邦政府，或者还是总<br />
统领导下的行政分支。我的经验是，大部分的场合，那还只是指行<br />
政分支。把“克林顿政府”读成“克林顿行政分支”，而且知道什<br />
么时候要把“美国政府”也读成“美国政府行政分支”，这是理解<br />
美国政治制度和权力结构的入门之课。 <br />
　　作为美国，自二百年前一开始，就令它摸不着头脑的那份华丽<br />
而厚重的大洋彼岸文化，至今依然使它困惑不解。比如说，在克林<br />
顿之前，就不知道已经有多少位美国总统，有幸被冠在“政府”二<br />
字之前，被成千上万的中国人误以为他们在美国有无边的权力，堪<br />
称“政府首脑”。但是，我敢跟你打个赌，你信不信，不仅以前的<br />
美国总统，就是今天的克林顿，对此也是一无所知。他以为全世界<br />
的人都清清楚楚地理解，他只是这个国家的一个“行政主管”。<br />
　　对于美国人，要了解东方文化最难的起点，也同样是如何了解<br />
对方的逻辑。如何把自己已经根深蒂固的思维方式暂时放一放，顺<br />
着对方的思维轨迹先走走看。这样，更容易找到合适的对话起点。<br />
　　两个大国之间的对话是无法避免的，躲得过初一，躲不了十五。<br />
我有时想想，何苦自寻烦恼，非要早早地在那里讨论，是要东方西<br />
化还是要西方东化。从我们自己作为一个平民的经验来说，我们首<br />
先看到的只是双方之间文化陌生，以及以此产生的几乎一触即发的<br />
误解。一旦产生争执，双方都已经气急败坏了，谈的还不一定是一<br />
回事，让人看了只可能产生荒诞感。还不如先想办法多去掉一点“陌<br />
生”和“荒诞”，然后再考虑要不要“化”的问题不迟。<br />
　　所以对于我们来自大洋彼岸平民来说，一踏上这块新大陆，自<br />
然感觉新鲜事就特别多。你就想想吧，就连“美国总统是什么”，<br />
都会成为一个问题，需要去了解的东西实在是太多了。<br />
　　在开始向你介绍今年的美国总统大选之前，先讲清楚他们选的<br />
到底“是什么”，相信你一定也会觉得这是必要的。这封信先到此<br />
打住。<br />
　　祝<br />
好！<br />
林达</div>

							
							
							
							
