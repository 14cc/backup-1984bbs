<br />
People Get the Government They Deserve<br />
24 February 2010 5 Comments<br />
长久不更新，借拍人的机会聊聊美国总统制<br />
<br />
―――――――――――――――――――――――――――――――<br />
<br />
不知道周克成又从哪里转来一篇浑身是洞的文章。<br />
<br />
而那篇文章一上来就给了一个荒谬的对比：人类历史上只有君主制和非君主制。废话这不是？世界上什么东西不是A或者非A？世界上的人都是白人或者非白人，中国人或非中国人。为什么不干脆换个”人类有史以来只有民主制和非民主制”呢？我倒好奇换了这个前提，这篇文章还能不能站住脚？写那文章的人估计LSAT能拿120分。<br />
<br />
任何国家的政治都不是一个制度构成的，英国固然是君主制国家，但英国同样是代议制民主国家，英国也同样是议会至上的宪政国家，英国还同样是单一制的国家。美国同样也是诸多体制构成的，总统制，三权分立制，宪法至上，联邦制都是美国政治的闪光点。按照亚里士多德的说法，君主制本身就是一个褒义词，暴君制才是君主制的劣性变种。如果要我在英美之间选择的话，君主制与其说是两者的差别，不如说是两者的共同点。美国总统制不就是抄袭了80%的18世纪英国君主制么？时过境迁，一直在权力的战场上开疆辟土的总统制早就比江河日下的君主制更强大也更危险，完成”沦为”不折不扣的”自由公敌”，在这里奉劝那些崇尚自由的”非主流民主派”们在选择自己心有所属的时候千万不要选择美国。<br />
<br />
虽然美国人推翻了英国的殖民统治，把君主制看做是危害美国人自由的洪水猛兽，而把代表各州选民利益的立法机构看做是自由守护神。结果在各州独立后颁布的宪法里都尽可能的把行政机关弄成了立法机构的附属机构，大多数州的州长只有一年任期，基本是屁股没坐热就下台了。所以别看那时候仿佛美国的政界精英个个都是州长出身，那时候州长廉价的很，根本配不上那年头流行的”His Excellency”的名头。可是没几年美国人民（准确是美国富人）就发现孱弱的行政机关不仅让各州议会先后落入”暴民”的掌控，各州议会为了应对独立战争后的经济危机（据估计这场危机其实比大萧条更厉害）死命的印纸币，大肆减免穷人债务，疯狂降低税率（200多年了，现在美国政府真是一点长进都没有），最后导致美国各州信贷市场极度萎靡，欧洲投资人宁可把钱借给穷奢极欲的路易十六也不用于遍地机会的美洲大陆，结果连麦迪逊这样的上流人士连买地的资金都筹措不到。这时候革命狂热症退烧的美国人才忙不迭的召开立宪会议，一个”精力充沛的行政机关”才提上日程。<br />
<br />
这个行政机关最直接的参照物并不是某些国内学者所言的罗马皇帝制，而就是他们推翻的英国君主制。虽然乔治三世不济，但英国君主制配合英国的两院制在18世纪还是当时全世界最优异的政制组合，而美国的参众两院+总统其实就是模仿英国的上下院+英王而来。而这个美国总统制从一开始就具备英王拥有的很多权力：议案否决权（虽然可以被两院2/3推翻），人事任命权（虽然需要参议院批准），外交独断权（虽然条约需要国会批准），战争指挥权（虽然钱袋子在国会）等等。可以说美国总统不过就是一个打了折扣的英国国王而已，而上面这些看似障碍的束缚早在200多年的历程中逐渐被总统打破。参众两院2/3的推翻简直比登天还难；需要参议院批准的人事任命仅限于部分职位；条约的确需要国会批准，那么私下的executive agreement就绕过了；打仗么，什么时候见过国会没掏钱的？<br />
<br />
所以当时就有人说美国总统分明就是民选国王，乔治华盛顿不过是”维吉尼亚王朝”的乔治一世（前五任总统四位是维吉尼亚州人，累计32年在朝）。而且看看总统制最热心的鼓吹者汉密尔顿，完全就是国王学院（现哥伦比亚大学）培育出来的King Maker。要不是华盛顿把持的住坐怀不乱，登基大宝的机会多的数不清。如果有空看看汉密尔顿在立宪会议上提出的宪法草案就知道那压根就是照搬英国体制了，说白了就是要让他尊若父亲的华盛顿黄袍加身。所以美国没有出现君主不过是天意，先是碰上华盛顿这么一个半神半人的伟人，再是恰好前五任总统都没有子嗣（唯一儿子长大成人的亚当斯的儿子是第六任总统），然后英国那时候也继续在和法国拿破仑死掐虽然1812烧了白宫但没有趁机”光复新大陆”等等。其实谁心里没有皇帝欲？淡薄如华盛顿还喜欢过His Mightiness, the President of the United States and the Protector of Their Liberties这样夸张的尊号。<br />
<br />
所以如果在中英美之间选择，在”非主流民主派”看来，英美的差别恐怕不过是美国的少数人暴政（总统滥权）和英国多数人暴政（议会独大）的差别而已，你们适合生活的地方应该是生机勃勃的神州大陆。</div>

							
							
							
							
