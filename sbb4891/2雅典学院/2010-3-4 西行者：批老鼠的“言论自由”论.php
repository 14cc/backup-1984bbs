													<h2>西行者：批老鼠的“言论自由”论</h2>
						
						
																											<div id="postmessage_501384" class="t_msgfont">原文链接：<a href="http://coninex.com/wordpress/2010/02/%E6%89%B9%E8%80%81%E9%BC%A0%E7%9A%84%E2%80%9C%E8%A8%80%E8%AE%BA%E8%87%AA%E7%94%B1%E2%80%9D%E8%AE%BA/" target="_blank">http://coninex.com/wordpress/201 ... %E2%80%9D%E8%AE%BA/</a><br />
<br />
这篇文章主要是回应不锈钢老鼠对言论自由的观点，谈谈自己对言论自由的看法。<br />
<br />
在老鼠看来，言论自由的唯一必要条件是国家不得干涉个人言论，那么两者的逻辑关系就是if you have freedom of speech, then the government shall not impede your right。<br />
<br />
那么逻辑上逆否过来就是如果国家限制你的言论，那么你就没有言论自由了。（原命题真则逆否为真）可实际上是，即使是美国在实践中也没有给言论自由这么大的定义。这也再次印证我一直认为的民运活动家们思维过于简单的看法，他们往往会忽略他们模仿对象的真实，想当然的去随意的理解和阐述自己掌握的有限信息。还有一个就是民运活动家基本都欠缺普通法知识，照本宣科的朗诵世界上最含糊的文献作为自己的论据，而不管在现实中的判例已经走到世界的哪一头去了。<br />
<br />
言论自由是美国宪法学中最复杂的一个部分，其涵盖范围之广，内容涉及之繁杂远超过老鼠文中提及的几个连正确都未必说的上的＂点＂。言论自由不是那种空泛的理论或者口号，而是和现实紧密相关的具体实践，任何有关言论自由的案件都有着各自独特的背景和现实。颠覆性言论是否受到宪法保护？诽谤性言论是否受到宪法保护？广播媒体的言论和传统媒体的言论保护程度有什么差别？在具有敌意的听众面前的言论该如何处理？和结社权利相关的言论自由的边界在哪里？集会情愿的时候如何合法行使自己的言论自由？淫秽言论是否受到同等的宪法保护？焚烧国旗抗议政府和在后院焚烧树叶是否有异同？政府公司工会是否拥有同等的言论自由？政治言论商业性言论含糊言论内容性言论观点性言论混合性言论在最高法院都有着独特的评估标准。<br />
<br />
谁说政府不保证每个人说话的机会了？公办教育的一个重要目的就是让公民受到最基本的教育以便他们表达他们自己的言论，因为只有自己才能捍卫自己的权利。每个没有受教育的人在表达自己政治经济权利的时候和聋哑人没有区别，他们的权利就随时面临着其他人的侵犯利用和剥夺。<br />
<br />
在涉及到媒体的时候，Fairness doctrine早就被高院拿来作为申案标准。不管是电视还是广播，都需要给观点另一方提供免费的答复时间，因为广播电视执照的所有人并不比那些没有广播电视执照的人享有更多的言论自由。电视台和广播台＂有责任发布他所在社区那些禁止从事该行业的听众们的观点和声音＂，电视广播商的权利是从属于电视观众和广播听众的权利的。这也是迫使内容供应商必须自我审查，尽可能减少敏感问题的报道，否则他们就要拿出措施来给事件双方平等报道或者等着打官司。在这里，听众们的权利可不干什么＂工具主人＂的事情，工具仆人还差不多。<br />
<br />
至于政府审查媒体也未必构成对言论自由的侵犯，在涉及到色情言论政治言论等情况的情况下，最高法院早在１９７８年的FCC v Pacifica Foundation的时候就认定广播电视媒体虽然享有一定程度的言论自由保护，但因为他们在美国人民生活中占有的独特地位，所以在其内容上自然受到最多限制。而且在１９９７年的判决中，高院再次维持原判支持国会立法要求有线电视台必须开放一定数量的免费地方节目给观众，尽管这些台压根不赚钱，在这里私人老板根本没权力说三道四。<br />
<br />
如果认为资本家为了赚钱就会单纯的迎合民众那就更是有点一根筋的思维了。民众并不是天生就已经具备了对任何事物任何事件的看法，然后等着资本家来办媒体迎合他们的。更多的时候他们对某些事情看法是逐渐形成的慢慢累积的，这也就给了媒体对舆论进行引导构架强调淡化设立优先级的机会，好比2003年只看FOX News的观众几乎一边倒的支持伊拉克战争，而在FOX News关注之前大多数他们的观众对此问题并没有看法，而相比之下当时看CNN的观众支持伊战的比例一直偏低，这自然就是媒体的作用。如果民众预先就具备了立场，那么也就不存在广告的效用了，广告的目的就是为了强化或者改变别人的观点，而并不是简单的迎合。而且民众缺乏足够的新闻来源，比如他们不可能亲身跑去伊拉克了解那边到底是怎么个情况，只能从媒体的二道贩子那里了解。他们也不大可能会跑去白宫打听各种小道消息，这还是掌握在媒体手上。虽然网络兴起降低了消息传播的成本，但消息来源却日益集中。随着美国的媒体全部集中在六家大公司手上之后，日后这些媒体公司在塑造舆论的时候更是翻手为云覆手为雨。在舆论的战场民众未必是优势的一方，更多的时候是被操纵和影响的一方，这在我们每天中国的生活都可以看出来，影响民众所想所欲所感正是掌握了话语权一方孜孜不倦在做的事情。否认资本家垄断话语权的可能性就像说中宣部不存在一样让人发笑，既然政府有动机有能力也有成效的去影响控制限制舆论，为什么资本家没有这个可能？他们一样会毫不犹豫的去影响控制限制舆论，只不过相比于“和谐社会”这样的动机，他们的动机更单纯：钱。<br />
<br />
言论自由和道德审美的关系就不多说了，美国无处不在的政治正确就是最简单的例子。上面FCC v Pacifica Foundation也是限制儿童不宜内容的言论的要案，你可以认为美国不存在言论自由，whatever。接下来有关诽谤的内容就更是异想天开了。谁说诽谤吗某个组织某个宗教某个群体是受到言论自由保护的？Beauharnais v. Illinois就说了，基于种族宗教等原因诽谤任何群体是不受宪法保护的。至于最后刘晓波那段就看前面那篇文章吧，里面引用的案例一个都没有被 overrule掉，易地而处如果有人在美国鼓吹共产主义让美国政府感受到威胁了，下场一样。<br />
<br />
我最烦的就是国内外的那些言必称美国的民运，三句话不离宪法。而其实他们压根就是叶公好龙，爱的是自己脑子里面所谓的美国，掩耳盗铃的蒙上双眼拿着所谓第一修正案来套一些在美国都不存在的事情。我早就说这群签署08宪章的完全不知道自己支持的是什么，从这篇文章字里行间处处透露着这一点，还打算以此烂文给宪章搞宣传，抹黑还差不多。要和中共搞，这群人本来唯一的优势就是真理和努力，而他们却从中共那里学来三脚猫的坑蒙拐骗浑水摸鱼的伎俩，躺在自己的“功劳簿”上缺乏对他们所向往的体制进一步研究和学习的动力。你们和政府爱咋咋，但拜托别再拿美国说事，美国出现在这些人的文章中简直就是给美国丢人。</div>

							
							
							
							
