关于本站<br />
<br />
Constitutionalist in Exile采自DC联邦巡回法院法官Douglas Ginsburg提出的著名宪法学概念：Constitution in Exile。该观点认为自罗斯福新政以来，经过民权运动时期最高法院对宪法的过度诠释，美国宪法实践已经过分的背离立宪国父们的原意，导致联邦政府权力过于庞大，州权受到限制和侵犯，人民逐渐丧失自治能力，一步步走向当年富兰克林在立宪会议闭幕上所预言的“以暴政而告终”的局面。我虽然不是Originalist，我现在的学识也远没有到可以给自己选择宪法学取向的时候，但即使在这些美国人看来如此不堪的宪法，我都有很多可以从每一个案例中取得收获。<br />
<br />
之所以选择这个名字，也是借此向当年漂泊海外的立宪派先贤梁任公致敬。在当年那三千年未有之大变局，相比于激烈的革命手段，梁公选择了一条更艰难更不易见成效的道路，毕生为中国的立宪事业而奔走，实为吾辈之楷模。经过近百年的数次变革，虽然我们在物质成就上和国际地位上实现了几代人的夙愿，但我们在宪政制度上取得的努力实在微乎其微。这也是我创立这个网站的目的，希望能通过这个窗口，让更多的国人了解美国宪政制度上取得的成就，经历的波折，消除来自左右两派对美国的错误解读。<br />
<br />
我不想在这里谈大道理，也不想利用这个网站宣传什么理念，但我很赞成刘瑜女士的做法，那就是多问一个为什么。相比于大量对美国浮光掠影般的介绍，本博更倾向于细致具体的介绍美国政治的制度及其运作方式，而不是笼统的给出一个结论。我相信我们从来不欠缺武断的判断，却没有得出该判断所必备的论据。在这里，我相信在读完我对每一个具体制度每一个具体案例前因后果的介绍后，读者会有自己的见解。当然，我虽然不致力于宣传理念，但不代表我没有自己的坚持和看法。<br />
<br />
除了宪法学，其他政治学领域的文章我偶尔也会发表在此，以美国政治为主。希望大家能多来看看，欢迎各位与我一起分享学习的心得体会。<br />
