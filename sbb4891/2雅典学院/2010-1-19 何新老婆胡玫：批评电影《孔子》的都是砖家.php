													<h2>何新老婆胡玫：批评电影《孔子》的都是砖家</h2>
						
						
																											<div id="postmessage_440270" class="t_msgfont">南方都市报 <br />
<br />
摘要：昨天，百家讲坛主讲人鲍鹏山在自己的博客上发文《为〈孔子〉，我闯祸了》。原因是电影《孔子》将于22日首映，他日前在看过内部场后对媒体发表了一番批评，表示《孔子》当中很多不符合历史真实，新闻见报后随即引发《孔子》的编剧何燕江、陈汗等人的迅速反驳。导演胡玫也在发布会上表示，所谓的专家，恐怕只是“砖家”。&nbsp; &nbsp; 本报讯 (记者 田志凌 实习生 王军)昨天，百家讲坛主讲人鲍鹏山在自己的博客上发文《为〈孔子〉，我闯祸了》。原因是电影《孔子》将于22日首映，他日前在看过内部场后对媒体发表了一番批评，表示《孔子》当中很多不符合历史真实，新闻见报后随即引发《孔子》的编剧何燕江、陈汗等人的迅速反驳。导演胡玫也在发布会上表示，所谓的专家，恐怕只是“砖家”。昨日，本报记者采访了鲍鹏山和《孔子》的编剧。<br />
<br />
&nbsp; &nbsp; 质疑一：孔子不该直呼弟子的“字”？<br />
<br />
&nbsp; &nbsp; 质疑：“对孔子这样的大人物，电影里很多基本事实竟然都完全不符合历史，我完全不明白编剧们为什么要这样做。”引发制片方强烈反弹的就是鲍鹏山的这番指责。<br />
<br />
&nbsp; &nbsp; 鲍鹏山称，《孔子》的错误很多。首先是连孔子儿子的名字都写错了。“孔子的儿子叫孔鲤，可是电影里他第一次出场的时候竟写成了‘孔锂’。”他认为另一个硬伤，是《孔子》里面出现孔子直呼自己的弟子为“子路”、“子贡”。“称呼别人的字，这是对同辈的称呼方法。但是孔子是长辈，喊弟子一定是直呼其名的。<br />
<br />
&nbsp; &nbsp; 回应：对此，《孔子》导演胡玫表示，孔鲤的名字是字幕打错了，的确是个硬伤，应当道歉。因为距离电影上映还有一周，对此会做修改。至于不该直呼弟子的“字”，《孔子》编剧何燕江说，关于片中人物的称呼，他们曾经专门讨论过。子路名叫卞仲由，但如果这样称呼，恐怕很少观众知道此人是谁。再如“颜渊”是名，但大家不熟悉，也就不如称“颜回”。何燕江说，作为编剧，必须把复杂的事情简化，说明白，重要的是让人看懂。<br />
<br />
&nbsp; &nbsp; 质疑二：南子是被谋杀的吗？<br />
<br />
&nbsp; &nbsp; 质疑：鲍鹏山还指出，片中孔子的弟子子路、颜回以及卫国南子的死都不符合历史的记载。片中子路是在卫国战死的，之后颜回在回鲁国的路上为了跳入冰水救孔子的书而死。鲍鹏山说，子路和颜回其实都是在孔子回鲁国之后死的，而且颜回大概死于贫穷。<br />
<br />
&nbsp; &nbsp; 鲍鹏山称，死得最离谱的是周迅饰演的南子。影片中，南子在内乱中被谋杀致死。“历史上，卫国的太子想要谋杀南子，但并没有成功，反而因为意图谋杀南子被逐出卫国。”<br />
<br />
&nbsp; &nbsp; 回应：何燕江回应说，这几个人的结局，除了子路之外，其他几个人的死，历史书上根本没有任何交代。这属于历史的空白，编剧才可以编。关于颜回，史料中其实只记载颜回早死，但并没说他怎么死的。鲍鹏山说他死于贫穷，并没有根据。<br />
<br />
&nbsp; &nbsp; 南子也是一样，怎么死的，并没有书证。“我们设计她中了一箭，就没再交代她的生死了。电影里容纳不了那么多，所以简化了。像夹谷会盟，五子台平叛，回鲁国，这些历史上有详细描述的东西，我们就完全按照史书上的内容去还原。而像孔子见南子时的对白，历史上没有的，我们就可以写，这个我称作‘钻历史的空子’。”何燕江表示。<br />
<br />
&nbsp; &nbsp; 质疑三：于丹的话成孔子思想？<br />
<br />
&nbsp; &nbsp; 质疑：片中，孔子两次说道“言必信，行必果”。鲍鹏山称，根据他多年的研究，这样的说法孔子并不赞同。片中当孔子被逐出鲁国时，弟子颜回“劝说”孔子：“您曾经跟我们讲过，如果人不能改变世界，那应该改变自己的内心。”鲍鹏山表示：“这话是于丹说的啊，怎么变成孔子思想了？这是要妥协屈服，孔子根本不可能有这样的思想。”<br />
<br />
&nbsp; &nbsp; 回应：何燕江称，“如果人不能改变世界，那应该改变自己的内心”确实不是孔子的话，但编剧也从来没有看过于丹的书，这是编剧为孔子虚拟的话。<br />
<br />
&nbsp; &nbsp; 鲍鹏山：作为知识分子，我有义务质疑<br />
<br />
&nbsp; &nbsp; 昨日记者连线鲍鹏山。他表示，作为一名知识分子，追求真理是知识分子的责任，他有权利也有义务根据他所学史料做出质疑。“孔子电影的编剧对孔子缺少敬意，他们需要熟悉《论语》，至少读上一遍。在电影中出现的硬伤错误，对于人们认识真正的孔子会产生一定的误解，尤其是中小学生。”<br />
<br />
&nbsp; &nbsp; 鲍鹏山还称，编剧对他的质疑做出的回应，完全是一种人身攻击，他很气愤。“电影是一个公共的产品，观众和其他任何人都有权对它作出自己的评论，我只是作为一个学者提出自己的见解而已。”<br />
<br />
&nbsp; &nbsp; 据悉，鲍鹏山在《百家讲坛》的《孔子是怎样炼成的》节目已经录制完，预计在春节期间上映。鲍鹏山称，到时观众可以通过这个节目认识历史上的孔子到底是一个什么样的人。<br />
<br />
&nbsp; &nbsp; 编剧陈汗：他可能懂孔子，但不懂电影<br />
<br />
&nbsp; &nbsp; 昨日，本报记者电话采访《孔子》编剧陈汗，他表示他们与鲍鹏山的区别在于“他可能懂孔子，但不懂电影。我们可能不懂孔子，但是懂电影。这才是我们争论产生的落差点。”<br />
<br />
&nbsp; &nbsp; 针对鲍鹏山说自己遭到攻击，陈汗表示“我对他是不太客气的，鲍鹏山质疑潜在意思就是说：你是笨蛋，没有读书。但我就是学中文的，香港大学中文系硕士；对于孔子所有的史料，只要他看过的，我们都看过。”<br />
<br />
&nbsp; &nbsp; 陈汗说，电影只是作为一个切面，将2500年前的孔子经过艺术加工以电影这个媒介展现出来，展现51岁以后的孔子面貌。如果说电影中对孔子有一些修改，就是说对孔子的不尊敬，未免太过苛刻。<br />
<br />
