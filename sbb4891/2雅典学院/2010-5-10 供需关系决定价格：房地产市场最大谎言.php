													<h2>供需关系决定价格：房地产市场最大谎言</h2>
						
						
																											<div id="postmessage_607367" class="t_msgfont">房地产市场进入第二阶段，第一阶段是成交量急剧下降，第二阶段是试探性降价，第三阶段是价格全面下挫，直到投资价格向消费价格回归。<br />
<br />
&nbsp; &nbsp; 5月6日，恒大地产在香港宣布，集团旗下所有产品将全线打折，以85折对外销售。这是楼市新政以来，第一个有大动作的大型房地产开发企业。此前已有开发商试探性降价。据统计，从今年4月至今，上海共有近四十个楼盘打出折扣让利消息，其中涉及绿地集团五个楼盘，最高折扣为8.8折，房价由此回归至去年年底的水平。保利楼盘“湖畔阳光苑”位列其中。虽然多数开发商表示不打算“跟风”，但在高负债率、低杠杆率的影响下，受到现金流严峻考验的开发商将不得不促销回笼资金。除非他们在土地款、开发时间、购房预收资金专款帐户等方面受到地方政府与银行的特殊照顾。<br />
<br />
&nbsp; &nbsp; 在投资市场，房地产作为投资品受风险收益规律控制，基本与供需关系无关。但供求关系决定房价的言论四处流传，甚至连住建部副部长齐骥5月6日在接受采访时都表示，“部分城市房价上涨过快的主要原因是供求矛盾突出，其中，不合理的需求占了比较大的比重。当然，这些城市的房价过快上涨也与一些地方和部门认识不到位、落实调控政策不力等原因有关”。<br />
<br />
&nbsp; &nbsp; 房地产市场果真由供求关系决定吗？这是最大的谎言。来看个简单的例子。<br />
<br />
&nbsp; &nbsp; 2009年上海楼市严重供不应求。根据易居中国的数据，上海商品住宅（剔除配套房、经适房）的成交量比2008年翻了一番，从900万平米左右上升到1800万平米左右，未能超过2007年的2090万平米左右。成交量大幅上升的结果是，2009年除9月外，基本每个月都供不应求。这种情况直到今年一季度仍未彻底改观，3月份由于新政预期，买卖双方急于出手购房，导致成交量与均价均大幅增长。根据汉宇地产的数据3月份全市商品住宅市场供应量为79.53万平米，环比2月大增239.3%，成交量为76.88万平米，环比上月增加132.94%，经过1、2月底市场探底观望后，<br />
<br />
&nbsp; &nbsp; 但4月份出现了根本逆转。统计数据显示，今年4月上海楼市出现2009年1月以来首次供过于求的局面。2010年4月上海商品住宅成交面积为102万平方米，比3月上涨了27.50%，比2009年4月大幅减少了42.32%。楼市在2009年1月至今首次出现明显的供大于求。4月逆转现象不仅在上海，在北京、深圳等一线城市同时出现。<br />
<br />
&nbsp; &nbsp; 据北京房地产交易管理网数据显示，4月30日北京二手房存量已经达到了历史最高的21295套，相比楼市新政前的14号上涨了18.9%。而据北京中原三级市场研究部提供给本报记者的统计数据显示，在4月14日以后的下半个月里，北京二手房市场新增客户需求新增下跌了八成。在北京中原回访的8000个从3月15日登记的购房客户中，明确表态短期内不再看房的比例达到了75%。<br />
<br />
&nbsp; &nbsp; 难道从3月到4月，市场的刚性需求出现本质变化了吗？难道开发商推出的楼盘出现异动了吗？恰恰相反，今年一季度为了稳住房价，开发商在房展会上不愿意大举推盘，以寻求5月黄金周的高价。<br />
<br />
&nbsp; &nbsp; 供需数量没有发生根本变化，但投资者的预期发生了本质变化。主要表现在楼盘抛盘增加，而入市购房者大幅减少。据北京房地产交易管理网数据显示，4月30日北京二手房存量已经达到了历史最高的21295套，相比楼市新政前的14号上涨了18.9%。而据北京中原三级市场研究部的统计数据，在4月14日以后的下半个月里，北京二手房市场新增客户需求新增下跌了八成。<br />
<br />
&nbsp; &nbsp; 成交量急速下降戳穿了两个谎言。一是房地产高价由刚性需求者支撑，二是房地产由供求关系决定。<br />
<br />
&nbsp; &nbsp; 投资客看到未来风险远远大于收益，不再出手购房，以投资客支撑的资产品定价体系土崩瓦解。山还是那座山，房还是那些房，可是突然之间，没人买了，击鼓传花的游戏戛然而止。投资者忽啦啦一撤退，今天的供不应求明天就惨变为严重的供过于求。大幅拿地、大碗喝酒的房地产企业傻了眼，他们不得不挂着不差钱的招牌融资再融资，也就是沿门托钵。这一幕在2008、2009年已经出现过一次。<br />
<br />
&nbsp; &nbsp; 此时，房地产定价不得不向刚性需求者看齐，一向被冷落的房价收入比也就成为重要的衡量指标。刚性需求者不会像手握巨资、拿着银行的按揭或者冒用消费贷款的投资客，高价购房的目的就是为了短期内以更高的价格抛房，他们购房的目的是为了自住，是一件真正的大件消费品，自己钱袋厚薄最重要。<br />
<br />
&nbsp; &nbsp; 抱着供求关系的唱本，会出现大笑话。5月5日，中国社会科学院和社会科学文献出版社在京联合发布了《中国房地产蓝皮书(2010)》，预测2010年全国房地产市场总体上供求偏紧，各月房价同比增幅呈现由高到低趋势。中国社科院城市发展与环境研究所原所长牛凤瑞认为，虽然近期在政策调控下，房价出现了回落，但供求矛盾可能持续20年，眼下出现的房价下行时间不可能持久，幅度不会太大。<br />
<br />
&nbsp; &nbsp; 是啊，如果未来还有3亿农民进城，中国房地产倒是不缺刚性需求。可是，他们需要的房子可不是什么投资品，现在温州太太炒房团支撑的高房价不可能由农民朋友们接盘。“蚁族”倒是有刚性需求，问题是，他们能够支付每平米两万以上的房价吗？如果禁止拥有投资性的二套以上住房，所谓的供应压力就是一句空话。当然，深圳市房地产新政已经告诉我们，一些地方政府并不真的打算这么做。<br />
<br />
注：通胀走完资产泡沫阶段，进入菜篮子、水价等，经济遭遇资产品泡沫下行与消费价格上升的双重压力。此时继续挤压资产品价格泡沫至关重要，房地产下行，我们面临的是严重的产能过剩。<br />
&nbsp; &nbsp; 统计局长提出改进房价统计，希望统计人忍辱负重、奋起创新，并呼吁全社会对统计这门科学和研究人员予以尊重。改进统计方法是好事，免得闹大笑话。不过，一项工作要靠忍辱负重，看样子，统计局认准这实在是苦差事。无钱无权无尊严，整个一个“三无”产品。但尊严，是靠行为争取的，任何的赐予都不是真正的尊严。<br />
