													<h2>教书匠沉思录之：中华文明之内核、灾难与出路</h2>
						
						
																											<div id="postmessage_178733" class="t_msgfont">理论物理学统领整个自然科学领域，当然是不争的事实，故而有“自然科学之皇帝”的美誉。然而，对于中国人来说，大多实在不太能领会理论物理学之美与精髓。自然科学之诞生源于两千五百年前古希腊大哲毕达哥拉斯，因为他向时人指出“自然即数”。所谓自然即数者，其大意为，虽然自然界万事万物之诸表象在空间中随时间而演化运动，不恒常，但这种表象的时间性――运动变化――并不是杂乱的，受到其背后不能被直观的恒常存在者支配约束，而且这种支配约束是一种严格数量的支配约束，用今天的话来说，就是永恒不变的是支配运动变化的事物背后的规律，而且这规律是严格数量关系的规律，故此，如果有人相信毕达哥拉斯没有错的话，那接下来要干的事当然就是去把这种数量关系的规律找出来，物理学于是就诞生了，自然科学于是就诞生了，当然，自然科学于是也就讲着数学语言。由此可见，理论物理学或者自然科学，实在在本质上应当算一门严格的形而上学。西方源于古希腊的文明内核实在就是形而上学，甚至于宗教亦然，譬如基督教，我常常怀疑中国的基督徒们是否真正能领悟真正本真意义上的西方基督教精髓。<br />
<br />
&nbsp; &nbsp; 如果让我们从现在一直上溯到中国文明的源头的话，那么，的确是没有什么可以用“哲学”这个外来词来称呼的东西的，当然也没有什么“哲学家”，如果实在要说有哲学家的话，那么，在下曾经论述过，老子勉强算得上半个哲学家。中国文明中没有形而上学的内核，实在的说，中国人实在是一个形而下的人种。追求形而上学的东西，那会被中国主流所嘲笑，客气点说，就会如《庄子》里指责的“大而无当”。中国人实在是喜欢实利的，中国人的观点中，凡事都应当“有用”，如果没有什么用的东西或行动，那会被嘲笑或责骂。中国人的一切活动都有一个目的，也是唯一的目的，“有什么用”，或者再通俗点讲，就是有什么好处，好处者，实利是也，物质利益是也，在中国，权力最终也是指向物质利益的。<br />
<br />
&nbsp; &nbsp; 看清了中国文明骨子里的这种形而下内核，就容易理解为什么凡形而上学的东西不但不能在中国产生，甚至形而上学的东西传到中国来，也必然被改头换面为形而下的东西。当然就更能理解，为什么在中国久长的历史中，竟然没有诞生物理学和数学，甚至于萌芽都没有。并且，中国久长的历史中，也没有诞生自己的具有广泛民众基础的真正宗教，而只是有更接近于巫术的所谓信仰，这点，如若不信，去读读屈原，或者，去看看沈从文的世界，或者，去湘西走走看。即使如佛教这样的东西传入中国，也不得不被改成近于巫术的东西才能得以推广。看看中国民众间的佛教，给佛、菩萨等上香进贡是希图得到它们的更大的回报，甚至于企图它们得到进贡后帮自己去报复对手，这是公开的行贿啊，这不是巫术是什么呢？更有甚者，看看今天的佛教寺庙里怀有真正不可告人而大手笔出手的官员，真是对世尊的亵渎！如果世尊能到中国走一回，不知道他老人家会不会被气死。――当然，我想不会的吧，因为世尊如果动气，也就不是世尊了，呵呵，题外话。中国目前的理论物理之所以如此之衰落，也当然是因为理论物理学实在是一门没有什么用处的学问，它的目的也不是要为了“有什么用”而去研究的。看看中国目前大学中理论物理专业之衰蔽！理论物理研究是需要大投入，且为期久远，何况即使长时间大投入后，也不一定能有什么结果，再说理论物理的任何研究成果也不会直接导致什么好处，因此，理论物理学注定在这个国度里不但官方没有兴趣支持，民间也没有兴趣，官方和民间都没有兴趣于理论物理，也等不得，官方民间都相信人生苦短，抓住目前的利益才是要紧。人们知道个牛顿、爱因斯坦可于茶余饭后笑谈一下表示一下敬仰就成，官方宣示一下两弹神七于民众，证明科技成果之辉煌，得出伟大正确光荣就万事大吉。都信奉“我死后哪管它洪水滔天”！<br />
<br />
&nbsp; &nbsp; 因为中国文明的内核就是形而下，看透了这点，不但容易理解中国人的生存为什么没有远大的追求――他们说这是虚无缥缈的追求――，也容易理解为什么中国文明史中，不追求思想，更不会给思想以优先权，压迫思想的自由了。因为是一种形而下为内核的文明，因而，中国人一切行动和意识行为都是直奔唯一目的而去的：物质利益！中国人就是为了实现满足肉体欲望的物质利益而生活着的。看透了这点，所以也就能容易地理解中国历史那一轮轮原地旋转的怪圈。“彼可取而代也”、“皇帝轮流坐，明天到我家”、“打天下，坐天下”、“枪杆子里出政权”……，等等言论不出现在这样一个由形而下内核形成的文明的民族统治下的大地上，那才真正是奇怪的。<br />
<br />
&nbsp; &nbsp; 与理论物理学一样，如果得到了那支配诸表象的规律，再应用这个规律于具体个别的表象上，就会把那表象看得很清楚，把握得很真切。同样，如果理解了中国文明的内核，明白了中国人是一个什么样的种族的话，那么，把这个应用于具体个案，那就真正洞若观火了。譬如，邓的改革罢。如果如邓自己宣传的或者如官方所言的，邓是一个真正的马克思主义者的话，那么，他当然知道马主义中所谓经济基础决定上层建筑，而上层建筑亦会阻碍经济基础的观点。当年文革结束，政权和经济都处于崩溃边缘，邓们已经几无出路。但是，邓是很聪明的，于是惊天的猫论就出台了，而且好象已经忘记了从前的所以诱人的种种许诺，并不允许姓资姓社的争论。白猫黑猫，抓到老鼠的就是好猫，这种观点，列位只要去四川民间一走，就知道已经流传了不知道几千年了。那么，什么是老鼠呢，都知道，就是好处，物质上的利益！无论什么样的手段都可以，反正只要捞到好处就行。看看，这是形而下必然导致的机会主义。形而下内核的文明必然是没有远见的，亦没有长远的眼光，它只着眼于当下的具体利益。为了保住权力，邓们必然要以中国人的机会主义者的方式处置之。但他们的聪明之处在于，他们明白中国人是些什么样的人，那就是，跟他们一样，都是只关注好处的人，而且是眼下的好处。所以，给他们一条路去争取自己的物质利益，当然，就会忘记其它。所以，按他们的理论应当在上层建筑和经济基础两方面同时进行的改革，却只见进行了经济方面的改革，却搁置或不准谈论上层建筑的改革。然而，机会主义者当然的眼光短浅，必然留下长远上的祸根，这种祸根在中国的当下已经显露得过分清楚和明白了，而且没有人不受其害，无论利益集团还是被压迫阶层。<br />
<br />
&nbsp; &nbsp; 形而下者必然是一个机会主义者，而且是保守机会主义者。如果没有影响眼下利益的情况出现，他们根本没有变革旧有事物和惯例的冲动或意识。因而大家可以看到的是朱深渊在台上的时候，之前还一直以自己的国有企业工人身份自豪的工人们，却一夜间失业沦为最穷困者。他们怎么就没有看到长远点的事呢？为了更大的利益和权力，当然，机会主义者之间的相互迫害、告密、压迫、伤害是在所难免的。因为有着手握重权者的利益需要的存在，所以这些工人注定要失业，而且被美其名曰下岗。同样的，农民工在城市里面的为生活作非人打拼，不但被当年的包含国企工人在内的人嘲笑、看不起、鄙视、压迫，而且，后来当这些城里的最下层人通过种种门路混进城管之类的二鬼子集团后，一披上那些衣服，立马又对于小贩无所不用其极，而这些小贩曾经是他们城市里的同类或农民工。前天看到一个消息，因为城管看到农民工因天气酷热而在吃饭的时候打光胴胴，立即遭受暴力对待。如果有一天，因为民怨大到威胁某些人的权力的时候，那么，这些城管立马就得滚回去跟那些最下层人呆在一起，而且还要帮人顶罪名。其实，上层又何偿不是如此呢，譬如陈良宇们的入狱。形而下导致的机会主义的文明，在其内部，没有人是可以不受害的。据说在波士顿犹太人屠杀纪念碑上铭刻着这样的话：<br />
“当他们来抓工会组织者的时候，我没有站出来反对，我想我反正不是工会的人。<br />
当他们来抓犹太人的时候，我没有站出来反对，我想我反正不是犹太人。<br />
当他们来抓天主教徒的时候，我没有站出来反对，我想我反正不是天主教徒。<br />
后来，当他们来抓我的时候，已经没有人能站出来为我说话了。”<br />
这是对机会主义造成的罪孽的何等反省和血泪指控啊。而我们的国人怎么能不沉痛反省一下呢？文革表面上是中共造成的史无前例的人为大灾难，其实，从骨子里来，我们的文明内核才是必然导致这种灾难的罪魁，在这种灾难面前，没有人是干净的。可惜，没有人反省，每一个人都在控诉着自己受到的伤害，但没有人控诉自己给别人的伤害――机会主义者当然会这样。<br />
<br />
&nbsp; &nbsp; 从中国人由其形而下文明内核必然导致的机会主义生存风格来反观，历史上大多人为灾难和惨祸，没有人是干净的，都是作恶者。目前状态如此之恶劣，难道与我们每个人没有关系么？如果国人再看不清这点，以及这点必然产生的来自文明本源上的原因，那么再来次“文革”并不是不可能的，再多次发生“文革”也是必然的。看看屠夫事件中的各位！凡有近似于官方言论者，五毛之恶帽立马加之，而不去求事实之真相，且忘记言论自由之基本人权；凡涉金钱，必然度之以为实现一已之私心的动机，无事实依据的基础上直接污人为骗子，而对别人，却要求着言论的依据；凡有行动者，必然猜度为求名者，不想到别人也可以认为自己发贴也是为了求名；甚至于有为了一时言论快意而忘记求真相之初衷，拉帮结派，同进同退，相互声援，粗口满天；更甚至于咬紧自己的开初主观观点而不放，为观点而观点者――您那点观点重于真实？您那点名望重于一切？；有为眼球而眼球，无长远打算，行动混乱，有始无终，恣意煽情，行事不知道要达到具体什么目标者......。官方呢，为了位子稳定，欺上瞒下，无视法律与公义，造案并且报复性司法，上下异心，控制舆论渗透网络，地方以维稳等理由劫持中央，中央欲图左右逢源，默许、纵容地方胡作非为，能压制则压制，不能压制了，则出面舍车保帅讨好民间平息事态.......。真正是“非我族类，其心必异”！<br />
<br />
&nbsp; &nbsp; 我们的出路在什么地方呢？痛未定而思痛，波士顿犹太人屠杀纪念碑上的铭文也许于我们是一种先知似的启示。把眼光放长远点，重新思考人生的意义，立定生存的根本，建立起个人的图腾，然后汇合成民族的新的文明内核，构成形而上的文明新架构。立定脚跟，不要怕缓慢，不要急于求成，只有机会主义者才急于求成。与一切的不公道非正义斗争，而不是保持沉默，但同时也得跟自己战斗，控诉自己。由此，也许我们的文明将走上一条新的道路，一条摆脱向来的原地轮回的道路。我想起了诗人的诗篇，铭记着，让我们上路吧。<br />
<br />
