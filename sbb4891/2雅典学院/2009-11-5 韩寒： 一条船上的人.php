													<h2>韩寒评论捞尸体事件：一条船上的人</h2>
						
						
																											<div id="postmessage_314844" class="t_msgfont">今天在网络上看见了一张照片，因为照片是由上自下慢慢显示，所以在很长时间里都看见了一个老头气势滂沱站在船上，指点江山，后面跟着一个人，随从打点。这不就是我们领导考察的架势吗？等显示完整才看见船底下拖拽的尸体。<br />
<br />
事情回到了湖北荆州的大学生救人事件。这个事情包括见人不久捞尸要钱的事情想必大家都已经非常了解。从这张照片里，我们可以解读出什么呢？<br />
<br />
第一，根据报导，船主陈波垄断了荆州段的尸体打捞业务，其他打捞船过来便会受到威胁。所有的经验告诉我们，在中国，垄断行业只有两个可能，政府行为或者政府参与。当然，政府不会设立捞尸部，所以，陈波之所以能再当地垄断，和当地政府之间很有可能是有关系的。另外，更具照片的分析，陈波的举手投足以及后面随从人员的相貌形态，都颇有政府处级以上领导从业经验的风采。而且陈波一口咬定，捞尸费一定是12000元，少一分钱都不干，而且见钱才给人。如果是正常做生意，假设是你，捞三具尸体，36000元，你至少能够抹去零头吧，这是人之常情，况且遇见这样悲惨的事件，而且还是在英勇救人的情况下，任何人都会动容。我认为，陈波之所以一分不让，可能这12000元中，有相当的一部分是打点给他的后台的，如果你只收了5000元，很可能他还要倒贴。而之所以要看见钱才给人，表示他非常害怕别人不给钱。有三个尸体打捞上来，其中有一个真的不给钱，常人的反应都是继续要钱，实在要不到也没有办法，因为至少赚了24000。但是陈波宁愿被人当模特狂拍照还不给尸体，除了此人道德品行恶劣以外，还可能是支持他垄断经营的后台知道打捞起的人数，按照人数收取打点费用，所以必须要收到这笔钱。<br />
<br />
当然，我只是在猜测，我没有证据。大的大贪，小的小贪，官商公贪，不就这点鸟事吗。<br />
<br />
还有一个可能性就是，这其实就是一家政府背景的企业，“荆州市长江水上打捞有限公司”，写的很清楚了，荆州市长。当然，这个也可以向荆州的吴局长了解一下。说不定大家都有股份。只有政府背景的公司才能又垄断又黑。<br />
<br />
第二，这张照片中，陈波一伙人很明显犯下虐尸罪，敲诈勒索罪，故意或者过失杀人罪。因为首先，没有人可以证明被捞起来的必然是尸体，因为他尚未开具死亡证明，而且距离事发当时并没有超过一个理论上没有生还可能的时间，也许这个人没有死或者还有抢救的可能。你可以说我狡辩，我不需要证明他还活着，但你必须要有证据证明他已经死了。在对方生死不明的情况下，陈波等人阻止了对溺水者进行抢救的行为，已经形成了杀人罪。<br />
<br />
第三，当地的公安，救护，消防部门完全有理由和能力对陈波进行强制性的行为，让陈波交还溺水者或者进行施救。连城管都能对商户下命令，何况公安呢，而当地的相关部门在事发现场的不作为，证明了他们和陈波之间非比寻常的关系。很有可能他们是一条船上的人，所以他们不能上一条船。<br />
<br />
第四，这些都是倒钩案等一系列事件的衍生产品或者周边产品，那就是好人没好报，唯利是图，人情冷漠，这也是中国社会的缩影，有人需要帮助，再不帮一把就活不下去了，有人出手了，结果自己也被拖下水，岸上的好人干着急，磕头下跪都没有用，资源被垄断，没有利益驱动，你连个尸体都没有。<br />
<br />
第五，建议中国的公民随身携带两万元，第一是因为最近物价飞涨，多带点钱总没错，第二是万一被倒钩需要交纳一万到两万的罚款。第三是如果有朋友落水或者自己落水，你可以将现金举过头顶，这样才会有半官方捞尸队对你进行打捞，如果你有幸被救，对方又非常有道德，把你和尸体当成了一个收费标准，那么你还有八千元可以在医院或者救护车上对自己进行抢救。<br />
<br />
第六，建议从中小学生就开始进行游泳课的训练。会不会游泳应该作为毕业标准。当然，我到现在还没有学会游泳，必须也要去学习。需要取代的是什么课呢？不是将游泳课合并入体育课，体育课是一定要保留的，但是要取消思想政治课。我们上了十年的思想政治课，学习了这么多的鸟毛，除了建起了一个鸟巢以外，我们学到了什么？社会风气依然很差，这说明思想政治课从教育的角度来讲是完全失败的，而且因为内容枯燥，编写乏味，观点虚伪，文笔干涩。而作为洗脑专用课程，它无疑也是相当失败的，因为他需要强加背诵很多完全没有记忆点的内容，反而引起了无数青春期少年的反感，乃是过滤词的巨大失算。既然不能洗脑，那还不如去洗澡，所以，建议教委将所有的思想政治课改为游泳课，强身健体，还学会一项求生技能，这门课程一举从害人变成救人，功德无量。</div>

							
							
							
							
