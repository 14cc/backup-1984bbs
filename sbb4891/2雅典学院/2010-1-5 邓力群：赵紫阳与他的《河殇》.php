<br />
一九九四年七月二十日，王军以“中国高尔夫球协会”副理事长身份出现在台北<br />
中正国际机场，成为中共“太子党”特务公开入台统战台湾的第一人。<br />
王军抵达机场时表示，他到台湾纯粹是“以球会友”、推动两岸体育和文化交流<br />
，但这位“体育界人士”却不务正业，他在台湾接触的朋友，大部分是共产对台统<br />
战的政经界大老。<br />
王军赴台是通过活跃于大陆的媚共台商牵线实现的。他到台湾後，由当时的立法<br />
院院长刘松藩侄子刘德成担任联系人，并由台湾亿万富豪辜振甫作保。到台当晚，<br />
设宴招待王军的是台湾高尔夫球协会理事长陈重光。陈其实具有双重身份，他既是<br />
党政大老，又是台湾富豪。第二天，七月二十一日，王军又陆续风尘仆仆拜访了台<br />
湾金融界头面人物：台湾商业总会理事长王又曾，以及全国工业总会、和信、富邦<br />
、霖国、新光等大财团的负责人。王渗透台湾的目的至此已昭然若揭，然台湾那些<br />
政客蠢货还蒙在鼓里做梦。<br />
<br />
* 王兵绑架、太子党恶斗香港<br />
<br />
王兵虽贵为王震的大衙内，但其锋头一直在王军之下。王军任深圳中信董事长时<br />
，王兵只是副董事长。<br />
八三年三月，中信、中国海洋航空公司、中国直升机公司、中国海洋石油总公司<br />
、中国南海石油联合服务总公司、深圳联发实业公司联合成立中国海洋直升飞机公<br />
司，王兵出任该倒爷公司董事长兼总经理。名义上，中海直做的是直升机买卖，但<br />
实际上却是从事秘密军火交易的黑帮企业。<br />
一九九五年六月二十四日，王兵在光天化日之下于深圳香蜜湖绑架了党匪头目乌<br />
兰夫的孙女婿陈显旋，香港电视迅即将此案作为共产黑色新闻予以曝光。<br />
这场党匪衙内的龙争虎斗，事涉深圳太子党的两个派系：一派是军头太岁王兵为<br />
首，一派则以广东地方势力叶选平太座吴小兰、李灏女儿李珩、乌兰夫孙女婿陈显<br />
旋当舵主。事情起因，无非是几千万赃款的分赃不均，最後酿成刺刀见红、香蜜湖<br />
大肉搏。<br />
事发後，邓三公主毛毛扬言要为王家兄弟两肋插刀，这可把专爱出风头的滑头江<br />
泽民吓坏了，乌龟脑袋缩到阴沟里至今没敢出来，故此案到现在没个说法。<br />
<br />
* 土匪窝里岂能出善类<br />
<br />
人所共知，大胡子王震是党匪头号丘八老军头，这个满嘴马列主义的反资产阶级<br />
自由化急先锋，在共产国却是数一数二的资产阶级腐化堕落份子。他生前，每逢冬<br />
