* 任琅 *<br />
<br />
“八九民运”提出的一个响亮口号就是反“官倒”。所谓“官倒”就是共产赃官<br />
、“太子党”，通过权钱交易化全民公产为个人私产的罪恶勾当。<br />
“六四”前夕，比较出名的官倒公司有，邓小平儿子邓扑方的“康华”、王震儿<br />
子王军的“中信”、李鹏儿子李小勇所在的武警公司，以及陈希同、李锡铭、王宝<br />
森为首的北京市委下属公司。“八九民运”要反“官倒”简直是断了这些共产头目<br />
的财路，因此党匪们为保卫自己的不义之财，不惜挥起屠刀大杀大砍手无寸铁的反<br />
“官倒”百姓。<br />
<br />
* 出身于党卫军摇篮的王军<br />
<br />
王军生于一九四一年，是王震次子。他从小不愿读书，是北京出名的体育棒子、<br />
学校混混儿。这，为他十年後以“球”到台湾“会友”，留下了伏笔。<br />
一九五四年，王军就读于党匪高干子弟云集的北京四中，三年的初中就靠玩球混<br />
了过去。由于成绩不好，高中时只好进了在北京排不上号的三十五中。不过王衙内<br />
有匪爷作後盾，他根本不在乎，六○年他终以太子身份被保送进了号称党卫军摇篮<br />
的哈尔滨军事工程学院。<br />
六五年，王军从哈军工毕业，进入了专事特务情报工作的共产军总参二部，开始<br />
了其子承父业的帮匪接班工作。<br />
<br />
* 炒房地产王军首开先河<br />
<br />
“中信”全称中国国际信托投资公司，这是共产借“改革开放”之名最早成立的<br />
一家官倒公司，并且还负有搜集情报、从事特务活动的使命。<br />
邓小平上台伊始，他令红色资本家荣毅仁出山，结合自己的嫡系下属共产军总参<br />
情报部（即二部）组成“中信”，荣出任董事长，总参二部副部长、著名特务头子<br />
熊向晖任副董事长，共产老牌外交特务米国钧担任总经理。王军就是在这个时候，<br />
以其太子党和特务专业的背景，被招揽进“中信”的。<br />
最早，王军任“中信”业务部经理，後晋升为副董事长兼副总经理和深圳中信董<br />
事长。一九八六年，共产尚未发明借炒房地产窃国，是王军首先看好了深圳的房地<br />
产叶，他利用特权把原本国有的、不允许买卖的土地变成了他名下的房地产，在深<br />
圳建立起了王氏房地产王国。就是从那以後，深圳楼价直线上升，全国各地随之掀<br />
起了房地产热，一时间原本无价的土地变成了赃官、太子党的滚滚财源。<br />
王军借房地产窃国成功，随即取代荣担任了“中信”董事长兼总经理。在他的努<br />
力下，“中信”很快成了共产国首屈一指的“官倒”公司和“太子党”公司。在那<br />
里，最多的时候，集中了共产司局长级头目子弟八十多人。<br />
<br />
* 向美国走私军火<br />
<br />
王军同时又是“保利科技”的董事长。“保利科技”实际是一家代表共产总参装<br />
备部的挂牌公司，专做军火生意，共产国的武器出口全都是通过“保利”进行的。<br />
王军在“保利”有一个最佳搭档，那就是邓小平的三“驸马”贺平 ---- 大土匪<br />
贺龙的儿子、邓榕的老公。贺在“保利”任总经理，负责主持“保利”的日常工作<br />
。<br />
一九九六年，美国爆出特大新闻，联邦调查局搜出了两千支共产国制造的AK47自<br />
动步枪，并逮捕了一批与走私这批枪枝有关的共产特务。“保利”被指控参与走私<br />
活动的，王军、贺平大名出现在被追捕者名单之首。从此，贺平在共产国销声匿迹<br />
，王军也名声臭臭。<br />
<br />
* 收购“新海康”、搅浑香港股坛<br />
<br />
九二年，邓小平声称要借搞活经济稳定共产政权，大陆许多官倒公司开始进军香<br />
港掘金，王军、贺平的“保利”自然不甘人後。<br />
九三年，“保利”来香港买入上市公司“新海康”。“新海康”是台湾许志勤家<br />
族属下的一家船运公司，“保利”斥巨资收购“新海康”，立刻引起香港股坛轰动<br />
。王向香港股民吹牛说：他要将“新海康”发展成“中信”在香港的旗舰。香港炒<br />
家因此闻风而动，“新海康”的股价最高被炒到了九元三角。<br />
然而骗子总归是骗子，随着“金融风暴”的到来，“新海康”终于显露原形。九<br />
八年“新海康”股票跌至最低价位六角钱，坑苦了那些跟风买入“新海康”的香港<br />
股民。<br />
<br />
* 捐款五十万、统战克林顿<br />
<br />
特务总忘不了特务勾当，一九九六年二月六日，王军“捐”款五十万，被邀进入<br />
白宫同小白脸总统克林顿“共饮咖啡”，向美国总统摇起了共产“统战”旗。王军<br />
自己吹嘘，他是由美国投资银行雷曼兄弟公司安排晋见克林顿的，但媒体却揭露王<br />
之能入白宫与献金丑闻主角崔亚琳有关。<br />
据美国“华盛顿邮报”报导，雷曼兄弟公司驻华盛顿办事处经理葛林是王军打入<br />
白宫的牵线人物，而葛同克林顿、崔亚琳都是老朋友，王军进白宫後的二月七日，<br />
葛的老婆就签了一张五万美金的支票给民主党。<br />
“洛杉矶时报”更指出，王军是规划共产全球进击战略的核心人物，他出大价钱<br />
拜见克林顿，其渗透、统战的目的显而易见。<br />
<br />
