													<h2>邓力群：赵紫阳与他的《河殇》</h2>
						
						
																											<div id="postmessage_415875" class="t_msgfont">赵紫阳辜负了陈云的嘱托和教诲<br />
<br />
1 赵紫阳不听陈云的两点建议<br />
<br />
陈云同志对当时的政治、经济形势是非常关心的。1987年7月，他两次向赵紫阳建议，一次是通过薄老转告，一次是亲自找赵谈，语重心长啊！陈云建议的要点是两个，一是讲你责任重大啊，死了多少人才取得了革命的胜利，耀邦没有把这个责任承担起来，现在由你来承担，你一定要想到牺牲了几千万人，要把他们的事业继承下去啊。陈还按着赵的肩膀说：你身负重任啊！第二点，陈要赵好好学哲学，并把自己从学哲学中所得的收益告诉赵，哲学、世界观的问题不解决，你的责任就承担不好。&quot;言者谆谆，听者藐藐&quot;啊！可以说赵完全辜负了陈的嘱托和教诲，也可以说陈的话不幸而言中。<br />
<br />
2 赵紫阳听不进陈云的八点意见<br />
<br />
陈云同志还在1988年提出过八条意见，这些都是针对赵在工作中出现的问题讲的，陈云开头就说：在我们这样一个社会主义国家里，学习西方市场经济的办法，看来困难不少。你们正在摸索，摸索过程中碰到一些问题是难免的，还可以继续摸索，并随时总结经验。八点意见，赵没有听进去。<br />
<br />
 <br />
<br />
赵紫阳欣赏《河殇》&quot;新纪元&quot;的说法<br />
<br />
在1988年还有一件事，赵紫阳支持的《河殇》，提出了一个新观点，即不是这一年，也不是那一年，而是赵紫阳上台的那一年叫“新纪元&quot;。赵紫阳不仅让有关单位复制录相带，而且亲自介绍、推荐该片给新加坡的李光耀，说值得一看。《河殇》播出以后，首先提出批评的是王震。王老在宁夏讲话批评，然后又找林默涵等人谈话，由林默涵等人帮助王老把意见整理成文章。文章写好给我看时，我就提出了这个意见，我说《河殇》的要害就是这一条，赵紫阳非常欣赏其中&quot;新纪元&quot;这个说法。李先念有一次见到我，说你对王老这个讲话、这个文章有什么意见啊？我说我提了一个意见，要害是&quot;新纪元&quot;的提法。先念说：哟，过去还不知道这个看法。<br />
<br />
赵紫阳下台时，王老发表了一个讲话，批判“新纪元”是讲话的内容之一。<br />
<br />
1989年6月21日，王震在中央政治局扩大会议上发表书面讲话，他指出：&quot;《河殇》的要害，是所谓'新纪元'。《河殇》里有两个'新纪元'。巴黎公社不是新纪元。十月革命不是新纪元，中华人民共和国成立不是新纪元。党的十一届三中全会不是新纪元，马、恩、列、斯、毛以及小平同志等老一辈革命家开创的事业，都不是新纪元，只有1649年英国资产阶级革命是一个新纪元。1<br />
<br />
987年赵紫阳同志当了总书记，又是一个新纪元；赵紫阳同志这个‘新纪元'，到底是什么样的'新纪元'？这是用'蓝色文明'即资本主义代替社会主义的'薪纪元'。这是削弱以至取消党的思想政治工作，瓦解以至'改造'党。反对邓小平同志代表的党，大树特树赵紫阳同志'新权威主义'的'新纪元'。这是把真正坚持四项基本原则、坚持改革开放总方针的力量打下去，把反对四项基本原则：歪曲和破坏改革开放总方针的力量庇护下来、扶植和集聚起来的'新纪元'。赵紫阳同志在经济、政治、意识形态工作中的错误，已经产生严重后果。几年来经济过热、通货膨胀、分配不公，社会治安混乱，一些党员、干部中发生贪污腐化现象，严重地脱离群众，人们的思想搞乱了、搞散了，早已在新中国绝迹的贩毒、吸毒、卖淫、走私、绑票、买卖妇女儿童、黑社会等丑恶现象重新泛滥，严重败坏了我们党和政府的形象，总祸根就是赵紫阳同志。到这次动乱和反革命暴乱，大大小小的野心家、阴谋家，反革命残余势力和社会渣滓纷纷出笼。他们打砸抢烧杀，破坏国家财产，惨无人性地殴打和杀害解放军指战员，把神圣的天安门广场和首都北京，搞得黑云滚滚。这是他们'新纪元'的一次大暴露。......赵紫阳同志是一个想开辟'新纪元'的野心家，还是一个善于耍弄政治权术的阴谋家。&quot;<br />
<br />
王老是产业工人出身，但是喜欢看小说、看理论书，敢于而且善于保护知识分子，在政治上也相当敏锐。<br />
<br />
