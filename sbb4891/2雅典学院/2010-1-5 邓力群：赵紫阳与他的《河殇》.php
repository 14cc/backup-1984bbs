																											<div id="postmessage_416211" class="t_msgfont">王震的长公子王兵因经济纠纷在深圳高尔夫球场公开持枪绑架陈显旋；邓小平的<br />
子女闹深圳、胁公安，巧取豪夺；更有大批二代、三代的太子布及大陆和海外，各<br />
领一批帮凶，鱼肉良民，横行乡里，无所不用其极。老百姓恨不得食其肉、寝其皮<br />
，但在“有了权就有一切，没有权就失去一切”的暴政之下，两手空空，只能忍气<br />
吞声。<br />
&nbsp;&nbsp;冰冻三尺，非一日之寒。太子党的形成，也不是朝夕之果。笔者当了四十多年教<br />
师，长期执教於高干子女学校，今天横行於海内外的若干太子、公主，正是昔日受<br />
我“教诲”之高徒！我现已退休，移居纽约，得以抚今追昔，对太子党的成长和形<br />
成的过程，做一简略分析。<br />
<br />
&nbsp;&nbsp;一、优越感浸入骨髓<br />
<br />
&nbsp;&nbsp;中共首脑居延安时，重要干部多去外地“开辟根据地”，为安置其後方子女，便<br />
成立了“延安保育院”，虽有一些特殊待遇，但总的说来，并不富裕，老百姓也无<br />
多少怨言。至於像毛泽东之子毛岸英，刘少奇之女刘允若、刘爱琴等人跳出战争环<br />
境，赴苏联留学，当然不是一般高干可以沾边的。但一因人数不多，二因赶上苏德<br />
战争，宝贝儿们反而吃了不少苦头，有的还被关进希特勒之集中营，所以也没有造<br />
成消极影响。<br />
&nbsp;&nbsp;待中共占据大陆，定都北平，情况产生了根本的变化。在北平，按两条渠道成立<br />
了干部子弟学校。一条是地方干部子女进的；像景山小学、育英中学（并非原来灯<br />
市口的美国教会办的那所育英中学，该校改称第二十五中），刘少奇同王光美生的<br />
孩子都在那里上学。另一类是部队的。最知名的两所部队高干子女寄宿制学校是北<br />
京十一学校和北京八一学校；前者隶属中央军委，後者隶属华北军区。朱德、萧华<br />
等人的子女都在“十一”（中共建国日）；叶剑英的小女儿（生於一九五零年）开<br />
始在“十一”，只在初中上了一年，但因其父不同意男女同班，便於次年转入男女<br />
分班的“八一”去了。<br />
&nbsp;&nbsp;八一学校位於北京海淀，原是一座清朝的王府，其中女生宿舍的居处假山嶙峋，<br />
流水环绕，亭台楼榭，书栋雕梁，人们认为是“红楼梦”中所写的大观园，并不确<br />
实。但大观园只是纸上美景，而“八一”确实是公子哥儿、姐儿的实实在在的人间<br />
天堂。假如说“八一”还算是对清朝旧址的利用，那么“十一”就不同了。中央军<br />
委在北京西郊军事区（旧称“新北京”）平地起仙境，依最新设计，创建了这所学<br />
校。当时北京人口爆炸式增加，城建部门规定不得盖平房，以节省占地。<br />
&nbsp;&nbsp;“十一”校长乃“十大元帅”之一的罗荣桓之妻，她亲自找到市长彭真说：“你<br />
不叫建平房，可你知道孩子们都小，上下楼梯摔着了，你要负责！”彭真听了，还<br />
能不给元帅夫人面子，慨然破例。教室为“工”形平房，两侧有门。外侧花砖铺地<br />
，绿树成荫，供晴天时孩子们出来玩耍；内侧是封闭式走廊，一端通宿舍，一端通<br />
饭厅，可以避开日晒风吹雨淋。学生起床後，将换洗的衣服一扔，由生活老师（实<br />
则是高级褓姆）送入洗衣房。不知怎么回事，洗衣设备全是当时被视为“头号敌人<br />
”的美国制造的！<br />
&nbsp;&nbsp;这些条件，在今天看来并不足为奇。而在五十年代和六十年代初，不知有多少人<br />
饿死的情况下，就不能不说他们已经走上脱离群众、背叛人民的道路了。孩子本无<br />
罪，但“墨悲丝染”，大人变成了新贵族，环境也贵族化了，他们的优越感的日益<br />
加深、侵入骨髓，自然也是无可避免的了。<br />
&nbsp;&nbsp;班主任有权批阅学生日记，下面便是几则日记原文：“每次同爸、妈去翠微路商<br />
场买东西，我总愿意在爸爸小轿车边多站一会儿，让行路人多看我几眼。”“老师<br />
算什么？还不如爸爸的警卫员官大！”“老师来家访问，爸爸挺客气。实际上，老<br />
师和我爸爸相比，只不过是蚂蚁和大象的关系。”从小有了这样的思想，长大了他<br />
去给人民当“公仆”岂非天方夜谭！<br />
<br />
&nbsp;&nbsp;二、狂暴性随机点燃<br />
<br />
&nbsp;&nbsp;单有优越感，还不致杀人放火。但是优越感中本有为虐的火种，一经点燃，便成<br />
了狂暴性，杀人放火便会成为小菜一碟。果然，契机真的到来了，点火人也到了。<br />
这契机便是文化大革命，这点火人便是毛泽东。一九六六年的文革初期，本来有个<br />
“十六条”，其中还没提到红卫兵，并曾提出“要文斗不要武斗”；但到了八月十<br />
八日毛泽东首次接见红卫兵时，毛泽东问献花的女红卫兵的姓名，回答说：“我叫<br />
宋彬彬。”毛又问清是“文质彬彬”的“彬”後，说了一句：“要武吗！”这个红<br />
卫兵便改名为“宋要武”，随之而生的正是全国一片打砸抢之风，草菅人命之风！<br />
红卫兵以“红五类”组成，而“红五类”之首乃为“干部子弟”，干部子弟之首又<br />
是军队高干子弟，这样一来，“十一”的红卫兵便自奉为“红卫兵之首”，而其它<br />
学校的红卫兵在集会中见到带有“北京十一学校”徽章的，便像伊索寓言中说到的<br />
青蛙遇见水牛一样，给予特殊的礼遇。<br />
&nbsp;&nbsp;“老子英雄儿好汉，老子反动儿混蛋”的信条，在他们心中是坚如磐石的。有意<br />
思的是这对联的横批，开始是“基本如此”，後来干脆改成“绝对如此”了。思想<br />
的荒唐导致行动的狂暴，假如说，火烧英国大使馆的事件为周恩来压住未致扩大的<br />
话，那么杀人如麻则是毫不夸张的实情实景了。下面就是当时我同我所教的一个男<br />
生的谈话情况：<br />
&nbsp;&nbsp;“老师！你当过兵，可曾打死过人？”<br />
&nbsp;&nbsp;“没有。我当兵之後没仗可打了，才转业教你们的。”<br />
&nbsp;&nbsp;“那可不如我，我昨天在教堂打死八个。”<br />
&nbsp;&nbsp;他刚十五岁（我教的那个班都是十五、六岁），手里提着一条浑圆的皮鞭，已经<br />
被血渍浸成乌黑色。他说话时是那么悠闲自在，哪里像杀人不眨眼的魔鬼？接着他<br />
向我描述了这样的场面：王府井旁边有一座天主教堂，红卫兵先将十几个修女递解<br />
出境（当时“人民日报”曾作为红卫兵的壮举加以报导），後来便住在那里。教堂<br />
里储存有罐头食品，吃饭自然不成问题。每天先到警察派出所问：“谁是资产阶级<br />
？”然後根据提名把这些“资产阶级”带入教堂，一带便是一家人。一家进去，立<br />
即被红卫兵分光。三个红卫兵揪一个，先将人推倒，有人喊：“一、二、三！”同<br />
时动手，皮鞭齐下，有专人拿着秒表计时，看各组打死人都用了多长时间，当然用<br />
的时间最少的是“冠军”。令我震惊的是：获“冠军”最多的竟然是宋彬彬等几个<br />
女生。<br />
&nbsp;&nbsp;宋彬彬是我平时喜欢的女孩子，她腼腆、娇小、作业认真、字体清秀，考试成绩<br />
也在班里数一数二。而现在，她居然能用皮鞭连续“作业”，数不清的“冠军”，<br />
连眼前这个男生也流露出“自愧弗如”的神情。人之变化，令人难料；毛泽东之“<br />
神力”，也确非虚传。十几岁的女孩子以杀人为愉悦，我至今不敢多想。更不知我<br />
一丝不苟地给他(她)们上课，换得这样结果，我的心血是不是该花费？<br />
&nbsp;&nbsp;这只是大海之一滴而已。“解放军报”发表评论，说红卫兵的革命行动“好得很<br />
！绝非糟得很！”接着又是来自中央的“除四旧，立四新”，“横扫一切牛鬼蛇神<br />
”的号召，打、砸、抢、杀的烈火燃遍全国。不能忘记这批干部子女，特别是像“<br />
十一”、“八一”这类学校的红卫兵，即军队高干子女，是功高盖世的！<br />
<br />
&nbsp;&nbsp;三、闷棍下学到狡黠<br />
<br />
&nbsp;&nbsp;为了将北京以外各地的阶级斗争盖子掀开，红卫兵“肩负”起了一项重责：去全<br />
国各地“煽无产阶级文化大革命之风，点无产阶级文化大革命之火”。与此同时，<br />
全国各地大、中学生，甚至部分小学生，也带上红卫兵袖章，或进京当“毛主席的<br />
客人”，或朝拜“革命圣地”井冈山、延安、瑞金等地，一场史无前例的“长征大<br />
串联”就这样崛起了。对一般红卫兵说来，大串联只不过是功课丢在一边，到各处<br />
“白相白相”，大游览一番；而这些干部子女因为肩负“重任”，自命不凡，又要<br />
高踞其他红卫兵之上，便真的有了不凡的经历。<br />
&nbsp;&nbsp;“十一”的几个红卫兵突然走进教导处办公室给飞机场打电话：“是机场吗？有<br />
去乌鲁木齐的班机吗？我们搭乘一下....我们是十一学校的红卫兵，新疆的阶级斗<br />
争盖子还没揭开，我们要见王恩茂....好吧，明天早上让飞机等我们。”<br />
&nbsp;&nbsp;他们打完电话出去了，我们几个老师却惊呆了：“搭乘”者，不花一分钱白坐飞<br />
机也！王恩茂者，集新疆党权、政权、军权、建设兵团权於一身之大吏也！还有，<br />
飞机要等他们去了再开！这几点作为，当老师的做梦也不敢想，而在学生身上都成<br />
了活生生的现实，真可谓“青出於蓝”了。<br />
&nbsp;&nbsp;大家都有这样的共识：这样的事，只有像十一学校的红卫兵才想得到，因为他们<br />
的父母是高干，使他们有这个境界，有这个知识，有这个“气魄”；而几十年後的<br />
今天也证实：正是靠这个境界、知识、“气魄”，才能够视江泽民的话为放屁，视<br />
公检法如草介，无法无天，我行我素。<br />
&nbsp;&nbsp;但就在这批红卫兵一步登天、不能自己之时，霹雳一声，受到了命运的捉弄。此<br />
时毛的眼中钉刘少奇等已被打倒，红卫兵已失去利用价值；相反的，红卫兵内部展<br />
开血肉搏斗，经济瘫痪，全国大乱，成为鸡巴毛炒韭菜 ---- 乱七八糟，影响了毛<br />
的统治基础和生存基础。毛轻轻说了一句:“现在该是小将犯错误的时候了。”<br />
&nbsp;&nbsp;其时，各单位都实行了军管和军训，野战军的小兵们(最大是个连长)夺了各部门<br />
的权。更出乎这批红卫兵意料之外的是：由於他们的父母都是军队首脑机关的头头<br />
，此时也被打成“资产阶级军内代理人”、“刘少奇伸向军内的黑手”，自杀的自<br />
杀，挨斗的挨斗，送干校的去劳改。後院起火，厄运轮到了红卫兵自己，这批高干<br />
子女让曾被他们“誓死保卫”的毛泽东一闷棍打了个懵头转向，像韩愈古诗中形容<br />
的那样：“失势一落千丈强。”<br />
&nbsp;&nbsp;饿死的骆驼比马大。他们早从父辈那里得到秘传：这是“运动”，“运动”过去<br />
，你还是你，我还是我。要善於“韬晦”，要能“硬着头皮顶住”。比其父辈早熟<br />
得多，也幸运得多，他们不到二十岁，便把他们父辈一生从中共内斗中获得的经验<br />
教训学到了。待再过二十几年，他们四、五十岁时，当然更“好汉”了：情妇成群<br />
，贿金盈库，口中念着：“我学孔繁森！”心中惦着:“好个王宝森！”<br />
<br />
&nbsp;&nbsp;四、分化中愈见风流<br />
<br />
&nbsp;&nbsp;事情总是画着圈儿走的，当他们的父母住够了“牛棚”坐够了“喷气式”之後，<br />
毛泽东发现：舍掉这批人，谁来充当爪牙？好在“人嘴两张皮”毛又轻轻说了话：<br />
过去把他们拉下马是对的，现在把他们扶上马也是必要的。於是他们的父母陆续被<br />
“解放”。只是正遇上知识青年上山下乡的高潮，他们难以抗拒。又是靠了父母的<br />
“经验”，不搞明抗，搞“曲线”救命法 ---- 走後门当兵。<br />
&nbsp;&nbsp;军队里熟人多，参军後能入党、提干照样飞黄腾达。没想到命运之神又给他们紧<br />
了一扣：参军没多久，就碰上对越战争。要去中越边境，要迎子弹，要刺刀见红，<br />
哎呀呀，我是为躲避上山下乡的，这下子却要住“猫耳洞”(一种简易防御工事)，<br />
那怎能行？好在这时他们的父母多又上了台，并逐步巩固了新得到的权力，更基於<br />
曾被打倒的经历，深悟出一条颠扑不破的真理：有权不用，过期作废，管他妈的老<br />
百姓怎么说，当进则进，当退则退，当初参军是对的，如今找个籍口，让孩退伍也<br />
是对的。於是，从孩子身上找出了“神经官能症”；於是，打电报说孩子妈“得了<br />
