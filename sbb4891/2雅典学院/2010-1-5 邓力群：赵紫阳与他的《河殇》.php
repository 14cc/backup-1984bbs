																											<div id="postmessage_433761" class="t_msgfont">五问 邓 力群<br />
<br />
――据《邓 力群 自 述 ━ 十 二 个 春 秋》<br />
<br />
（定稿2006/04/30）<br />
<br />
冯兰瑞<br />
<br />
&nbsp; &nbsp; <br />
<br />
 <br />
<br />
 <br />
<br />
世之政治人物，其实与常人一般，总是活动在一定的社会环境之中。个人经历的历史事件，自以为机密、自以为不妨操纵的事，必然多少会有人参与，会有人知晓。因此之故，往事想要修改涂抹，不免捉襟露肘。“要得人不知，除非己莫为”，寻常一句民间俗话，道中了古今多少人物的心事。有鉴于此，明智者撰写或口述历史就会留意，不敢胡编乱造，不敢脱离、更不敢违背而是一定要严格尊重历史，处处符合事实。否则总会有知情者、相关人员或细心的读者提出质疑，捅破窗户纸，岂不尴尬？尊重历史，无非常识。然而，如今却有权威自居者回忆往事而任意编造，吹嘘抬高自己，贬低攻击别人。以为自己所作所为或参与的事件无人知情，或则知情者慑于权威、碍于情面或其他考虑，不便或不敢说。可笑的是，该权威还特别突出强调自己的回忆真实可靠，以求欺世盗名。<br />
<br />
这番议论实有所指。所指非别，即《邓 力群自述：十 二 个 春 秋（1975――1987）》（以下简称“自述”）的作者。这位曾经的党内高官、“理论权威”的这部大作，正是歪曲历史的典型力作。作者大言不惭，宣称“自述”要“还历史的本来面目”，为“撰写中华人民共和国和中国共产党的这段历史，留下一份可信的史料。”（“自述”前言）读之令人哑然。<br />
<br />
“自述”长篇大论，内容丰富，与此相应，则问题层出不穷；要清理，有如恩格斯写《反杜林论》啃一个很大的酸果，费时费力。本文仅就其中略举数端，理清部分历史事实，请“自述”作者回答，并就教于读者。人间是非，天下公断。<br />
<br />
 <br />
<br />
一问邓 力群： <br />
<br />
我是“跟你一起”到国务院政研室工作的吗？<br />
<br />
“自述”开篇第一章谈“整顿中的国务院政治研究室”（简称政研室），邓 力群写道：“……很快国务院政治研究室就成立了，这个摊子就散了。这批人中间跟我一起到政研室的是冯兰瑞。”<br />
<br />
这于我算得小小一条“新闻”。 <br />
<br />
我是中央组织部分配到国务院政治研究室去的。1975年7月下旬的一天，于光远通知我说：“你的工作分配到国务院政治研究室。明天你就到中南海上班。”并告诉我政研室在武成殿附近，要从中南海北门进去。我没有正式工作已八九年，一旦分配工作且是研究单位，很是兴奋。<br />
<br />
那么，我到政研室又是谁提名的呢？我原来想当然地以为是老朋友于光远。上班几天后，我向于光远问起此事：“是谁提出的？”于光远答：“是胡乔木。”他告诉我，我到政研室是胡乔木提名，组织部分配的。此事与邓 力群没有关系。“自述”作者下笔时应该是清楚的。<br />
<br />
&nbsp; &nbsp; 有一桩往事与此相关。2000年，我写《在国务院政治研究室的日子》一文，发表于《百年潮》第三期。文中谈到《论总纲》的写作，认为此文是集体写的，批评《邓力群文集》将它作为其个人作品，放在该文集第一篇，不加任何说明，这种做法就不大好。听说邓 力群看到后很不高兴，他还责怪当时《百年潮》社长郑惠不替他说话。<br />
<br />
本来到什么单位工作，跟谁一起去，都是无所谓的事。我在胡乔木家编书并没有见到邓 力群。那他何以在“自述”中要这样写？无非是说你冯兰瑞到中南海国务院政研室，还是“跟我”去的。你不感谢我邓力群，反而批评我，岂不“忘恩负义”？事实是，我去政研室与他无关。退一步说，即令是“跟他一起去的”又怎么样？其中难道有何恩惠可言？这自然是小事，但从中也就可以看出“自述”作者的心机。<br />
<br />
&nbsp; &nbsp; 关于《论总纲》一文的写作问题，留待下文。这里要说说上面引文中提到的“这个摊子”。它是指1975年春胡乔木约请帮他编语录的五六个人，摊子就设在胡乔木家，我曾应邀前去参加编《马恩列论生产关系》语录。之前，我接到邓力群一个电话：“冯兰瑞同志，胡乔木请你去帮他编书”。尽管在延安时我已因听说一桩丑闻而久闻邓 力群大名，与他并未相识。要说此后有点关系，那就是1975年初接到的这个电话，可能是胡乔木委托他传话。总不能因此就认为我到政研室工作是跟他邓力群一起去的吧？<br />
<br />
&nbsp; &nbsp; 我与胡乔木认识则在很久以前。1940年我到延安中央青委工作，胡那时任青委宣传部部长，不久即调去当毛泽东的秘书。1975年我又帮他编了半年多书，可以说是熟人了。他提名我到政研室是很自然的。<br />
<br />
&nbsp; &nbsp;<br />
<br />
&nbsp; &nbsp; 二问邓 力群：<br />
<br />
&nbsp; &nbsp; 是谁，在“文革”后期向邓小平提出大学从高中招生的主张？<br />
<br />
邓 力群说，是他。“自述”第三章第4节“邓小平谈科学、教育”中称：“我向小平同志说，为了应急，应付青黄不接，打算在65-67年高中毕业的学生中采取自愿报名、严格考试、硬性抽调的办法，吸收进大学，培养一批人才。”&nbsp;&nbsp;<br />
<br />
&nbsp; &nbsp; 是这样么？<br />
<br />
&nbsp; &nbsp; 请看事实。<br />
<br />
十年“文革”，毛泽东说“知识越多越反动”，书读得越多人越蠢。知识遭到贬低，读书号称无用，教育制度受到严重的破坏。各地区各学校以及全国媒体大肆宣扬“白卷英雄”张铁生，连篇累牍赞美黄帅日记。在当时这种氛围下，学校大搞教育革命，高初中毕业生（共约一千七百多万，时称“知识青年”，简称“知青”）必须上山下乡劳动锻炼，接受贫下中农再教育。<br />
<br />
1975年，文革结束前夕，邓小平主持中央工作，全面领导各项工作，彻底进行整顿。邓小平指派胡耀邦、李昌、王光伟三人到中国科学院担任领导小组负责人；其中李昌除分管中科院日常工作外，还分管中国科技大学，而科大当时正在准备招生。1975年7-8月间，李昌就向邓小平提出了科技大学恢复招生的生源问题，他说：“我们打算直接从65-67两年的高中毕业生中录取，采取自愿报名、严格考试、半工半读的办法。”邓小平赞成说，“这个办法好。”<br />
<br />
李昌自五十年代以来长期从事高等教育工作，积累了丰富的经验，重视人才培养。如今作为中科院领导，分管中国科大，从培养中国的现代化建设人才以应国家经济发展的长远需要出发，不能不考虑入学新生的知识质量，这件事他责无旁贷。他看到高中学生下乡劳动固然可以受到锻炼，接触社会，但几年不上学，势必丢荒在中学打下的学业基础。所以，他提出恢复高考、直接从高中毕业生中招生的主张也就势有必至。在当时形势下提出这样的主张，是大胆的，也是非常必要的、迫切的。<br />
<br />
&nbsp; &nbsp; 据邓 力群的“自述”，从高中毕业生中直接招生却是“我”的“打算”。那么，这个主张究竟是谁向邓小平提出的？是邓力群还是李昌？<br />
<br />
事实俱在。这一建议由李昌向邓小平提出，有关情况当年中科院的吴明瑜（国家科委前副主任）、明廷华（国家专利局前副局长）等不少同志都知道。知情人现均健在，认真的读者可以调研查证。<br />
<br />
证据之一，《十年“文革”资料选编》之三，收录了《邓小平听取胡耀邦汇报科学院〈汇报提纲〉时的插话》。这个材料有这样一段话：“耀邦同志汇报完后，接着李昌同志汇报了几点：一是请中央调些干部，一是想在怀柔办一个科技学校，招高中生，半工半读，加强自然科学基础和外语的学习；一是办进修班，把各行专业在科研上表现特别突出的调来培养，一是办一个宣传自然辩证法的刊物。”这个《十年“文革”资料选编》是80年代为起草历史决议，由中央文献研究室编辑的，内部印发，印量很小，但是邓力群应该有。实际上，当年参加过这次汇报的好几位亲历者都有记录，而李昌汇报的几点，几个记录稿几乎一字不差。<br />
<br />
证据之二，当年“批邓、反击右倾翻案风”运动中，中科院运动办公室编了一个《胡耀邦、李昌同志部分言论摘录》（1976年2月）的批判材料，第七部分“关于教育革命问题”，摘的一段言论是：“胡耀邦、李昌同志准备建一所科学技术专科学校，要从各地直接选拔高中毕业生中数、理、化学得好的所谓尖子入学。……李昌同志还要科大也直接从高中毕业生中招生，并要科大办理科中学，培养尖子。”批判材料的编辑者当然是把这些作为“右倾翻案”的反面材料，却反映了历史的真实。<br />
<br />
证据之三，是胡乔木1976年2月23日在政研室全体会议上揭批邓小平的长篇发言。1975-76年“批邓，反击右倾翻案风”，主张和同意大学直接从高中招生曾经列为李昌和邓小平一条共同的罪状。胡乔木在他上述揭批的第四个问题中称：“他（邓小平，下同）说李昌主张科技大学直接从高中招生，实行半工半读，他赞成。他说半工半读是毛主席主张的，不会有问题。” 往事难忘。胡乔木在室全体会上揭批邓小平，连带李昌，言犹在耳。曾几何时，实践一旦证明这个主张的正确和超前性，它就变成为“我”（邓 力群）提出的了！胡乔木的揭发发言，当年在政研室印发，文件俱在，白纸黑字；我已经把它作为附录收进拙作《别有人间行路难》中（时代国际出版有限公司，2005年，第49页）。读者有兴趣，不妨查阅。<br />
<br />
邓 力群“自述”本身也经不起推敲：“‘我’打算在65-67年高中毕业的学生中采取自愿报名的办法，吸收进大学，培养一批人才。”请问，这是谁的“打算”？是邓力群吗？1975年他并不是任何高校的负责人而仅仅是政研室的七个负责人之一，政研室当年七月五日才成立，并没有办什么学校，邓小平也没有叫他管学校，他怎么会有这种打算？ <br />
<br />
&nbsp; &nbsp; 历史不容也无法篡改。邓 力群如此“自述”，无非邀功抬高自己，标榜自己正确。这样做诚然徒劳。却不免搅乱历史事件，混淆视听，给后人的研究平添困难。<br />
<br />
 <br />
<br />
&nbsp; &nbsp; 三问邓 力群：<br />
<br />
凭什么说马列所成了“反毛泽东思想的第一所”？<br />
<br />
&nbsp; &nbsp; “自述”第五章“在中国社会科学院”中，说到社科院新建了十几个所时，写道：“这时候（1979年―笔者注）还建立了马克思列宁主义毛泽东思想研究所。胡乔木原来设想要把这个所办成社科院的第一所，后来一段时间内却被苏绍智等几个人搞成了反对毛泽东思想的‘第一所’。”<br />
<br />
马列所竟然“被苏绍智等几个人搞成了反对毛泽东思想的第一所”。此种说法不仅是胡耀邦平反冤假错案后新增政治冤案，也真算得上头条新闻！虽然，邓&nbsp; &nbsp;力群无中生有，却使笔者得以弄清一桩重要历史公案：制造1983年马列所危机的罪魁祸首原来就在这里，“自述” 的作者正是其中一人（此事不排除胡乔木参与，他说过：马列所这些人怎么能研究马克思主义）！<br />
<br />
马列所危机，指社科院曾打算撤销马列所。（1983年《马列所的危机》笔者曾有专文记载，参见《别有人间行路难》第204-224页）<br />
<br />
按照邓 力群等人的逻辑，既然马列所已成了“反毛泽东思想的第一所”，当然应当撤销。但是当时邓 力群、社科院领导都没有说明何以要撤销马列所。如果不是所整党领导小组硬顶住，马列所糊里糊涂地撤销了，还不知道祸从何来。<br />
<br />
当初，马克思列宁主义毛泽东思想被定为我国的指导思想，共产党为了掌握意识形态以及开展社会科学研究的需要，胡乔木等就任中国社会科学院正副院长后不久，扩大社科院，新成立了十几个研究所，包括马列所在内。<br />
<br />
1980年3月，我来到马列所担任副所长兼党委书记。之前，已听说胡乔木要把马列所办成社会科学院的首席（第一）研究所；由副院长于光远兼任马列所所长。胡乔木指示“马列所需要的研究人员，可以从院内调，也可以从全国调，需要谁就调谁。”果然，调了《人民日报》社理论部的苏绍智来当副所长，以后又陆续从全国调进了一些研究人员，可谓“兵强将勇”。<br />
<br />
但成立不过五年，马列所就遭遇被撤销的危机。1983年10月16日我出访联邦德国归来，听到社科院内外传说马列所这个首席研究所要撤销，所内议论纷纷，人心惶惶。奇怪的是，马列所领导却得不到确实的信息。这时整党已经开始，院里派来三个人的联络组帮助马列所整党。我先是问过联络组，后来又在一个会上当面问过马洪（时任院长）：社科院是要撤销马列所吗？为什么要撤销马列所？组长说不清楚，马洪说院党组议论过，却说不清撤销的原因。<br />
<br />
实际是，社科院领导确曾议过撤销马列所的问题，已经研究撤销后人员的去处，打算让他们分别到经济研究所、哲学研究所和中央编译局，而且已经征求过此三单位的意见。马列所所长苏绍智（这时于光远已不兼任所长）、党委书记彭克宏曾接到该三个单位领导的电话，均表示欢迎这些同志去他们那里。<br />
<br />
了解这些情况之后，我认为第一，社科院领导的做法是违背整党决定的。按照党的十二届二中全会作出的整党决定，整党主要是“清除精神污染”，组织处理放在运动后期。而社科院在运动初期整党刚刚开始、马列所有没有“污染”的问题尚未查清以前，就要对它作出组织处理，是完全错误的。<br />
<br />
第二，我很怀疑：撤销社科学院马列所是谁的主意？<br />
<br />
我认为不会是马洪或梅益（党组书记）的主意。他们不具备这个权威，也没有这个胆量。那么，是谁有这么大的胆，竟敢撤销胡乔木如此重视的社科院第一研究所的马列所？为什么要撤销马列所？对此我一直不明白。当年有些想法，但未能作出判断。时值前兼所长于光远出国访问，无处可以得到确实信息。<br />
<br />
不料二十三年后，邓 力群不打自招。<br />
<br />
“自述”使我恍然大悟。正是这位前副院长邓（可能还有前院长胡）认为马列所被苏绍智等几个人搞成了“反毛泽东思想的第一所”，出了这个主意。真正是闻所未闻！<br />
<br />
邓 力群和胡乔木轻轻地使出一招“杀手锏”，可把我们害苦了！<br />
<br />
想当年，我同马列所的党政负责人（即整党领导小组五人），为了马列所的生存而苦恼焦虑、而奔走呼吁。彭克宏还陪同我去拜见薄一波请求支持，并曾两次上书邓小平、胡耀邦（第二次还有胡乔木）。我们一面领导整党学习；一面分析研究形势，想办法，写报告；还要为稳定同志们的情绪而做大量的思想工作。<br />
<br />
我还特别致函胡乔木请求他过问此事，让社科院不要撤销马列所。为了快速和可靠，我亲自将信送到胡的府上。这时我相信胡乔木是起决定作用的人，但还不敢断定撤销马列所就是他的主张。现在看到“自述”中的这段话之后，可以作出判断了：撤销马列所是胡邓二位的主张，此两人正是制造马列所危机的罪魁祸首。<br />
<br />
我还要问，既然马列所被苏绍智等人搞成了反毛泽东思想的第一所，那就是违反了四项基本原则。这么严重的问题何以邓 力群当时不向社科院或马列所提出批评，堂而皇之地将它撤销？社科院为什么含含糊糊不说出撤销的理由？邓力群攻击苏绍智等“几个人”，那就不仅仅指副所长苏绍智一人而是攻击了一连串的人：兼职所长于光远、副所长、党委书记冯兰瑞、兼职副所长王惠德和廖盖隆等人。他这话当时不说，至少是不敢向我们透露，这又是为什么？除了自己心虚，乱扣大帽子站不住外，还能有什么别的解释吗？<br />
<br />
&nbsp; &nbsp; <br />
<br />
四问邓 力群：<br />
<br />
1977年5月24日，邓小平到底会见了谁？<br />
<br />
&nbsp; &nbsp; 需要特别强调指出：邓小平第二次复出之前，于5月12日约见了中科院的方毅和李昌（“文革”结束后，中央调整了中科院的领导班子，胡耀邦已另有重任。中科院院长兼党组书记仍是郭沫若；方毅任副院长兼党组笫一副书记；李昌任副院长兼党组副书记）。5月24日则会见了国务院政治研究室的于光远和邓力群两人。历史如此，事实如此，改无可改。<br />
<br />
1977年5月初，政研室岌岌可危。党中央副主席和国务院已决定撤销这个单位，几位负责人已是束手无策。我碰巧知道了邓小平约方毅和李昌去谈话的事，就考虑政研室的负责人是否也去见见邓小平，也许能保全政研室。于是我就向政研室负责人之一的于光远建议，并代为打听到邓小平秘书王瑞林的电话，告诉了于光远。<br />
<br />
5月24日，邓小平会见于光远和邓 力群。当时有王震在座，而胡乔木没有去，只是托于、邓转达了他对邓小平的愧疚之情，邓小平表示对胡谅解。邓小平同他们亲切谈话，于光远当场作了记录，以后将记录提供给文献编委会，摘编入《邓小平文选》。以上情况，笔者已有专文《邓小平关于政研室的一次谈话》较详叙述（载《炎黄春秋》2004年第11期，并收入拙作《别有人间行路难》一书第84-90页）。事实十分清楚，此处不赘。<br />
<br />
意外的是，事过二十多年间，却有邓 力群控制下的某些人前后几次胡搅蛮缠，张冠李戴，弄乱了套。其中首先是中共中央文献编辑委员会1983年出版《邓小平文选（一九七五―一九八二年）》（亦即后来的第二卷），书中收进了1977年5月24日邓小平会见国务院政研室两位负责人于光远和邓力群时的两段谈话记录。此两文的题目是《“两个凡是”不符合马克思主义》、《尊重知识，尊重人才》。两篇文章都是1977年5月24日邓小平和于光远、邓 力群谈话记录的摘要。两文首页注脚都说：“这是邓小平同志同中央两位同志谈话的一部分。”这两个注脚引起了我和老伴李昌的不解和怀疑。据我们所知，1977年5月24日去邓小平处谈话的两位（政研室的于光远和邓力群）都不能称为“中央同志”。那么，这两位“中央同志”是谁呢？<br />
<br />
其次，1998年中共中央文献研究室编辑出版《邓小平思想年谱（一九七五至一九九七）》，其中1977年5月24日条目将邓小平会见的两位“中央同志”拔高为“中央两位负责人”。小平同志那天会见的同志的地位改得愈来愈高了，愈发令人疑惑：究竟邓小平那天会见的是哪两位“中央负责人”呢？<br />
<br />
第三是2004年，同一个中共中央文献研究室于8月份出版的《邓小平年谱（1975―1997）》记载，1977年5月24日邓小平会见的两位“中央同志”非别，乃是王震和邓力群，尽管1977年时这两位都还不是中央负责同志（这一点笔者已另文指出）。疑团解开了，《年谱》所述却又转而掩盖了另一真相，正是“千呼万唤始出来，犹抱琵琶半遮面”。这琵琶盖住了谁呢？原来，他们想要遮盖的就是当年与邓力群同时去见邓小平的于光远！那么，《邓小平年谱》的编辑们怎么会将于光远从邓小平当时会见的三位（于光远、邓 力群、王震）中排除，只留下两位了呢？这又是一个谜。<br />
<br />
&nbsp; &nbsp; 最有意思的是，邓 力群通过“自述”自己揭示了这个谜底，也说明编者何以敢于如此篡改邓小平同志政治活动的事实――因为有当年的“理论权威”、著名的邓力群担任幕后指挥，充当强硬后台！我曾听说，中央文献研究室曾就此事访问过于光远。于老对他们说明了情况，提出了异议，而文献室人员又去访问邓 力群，结果还是遵从了邓力群的意见。<br />
<br />
事隔29年后，邓 力群对此次会见又是如何“自述”的呢？他写道：“邓小平在听了王震（当时的国务院副总理）的反映后，采取了三个动作。”其中第一、二两个动作与本文无关，不谈。“三是让王老和我（邓力群―笔者注） 去看他” （见“自述”第三章“国务院政治研究室解散前后”中“邓小平的三个动作”）。原来如此！这里不是政研室的负责人于光远、邓 力群为了政研室存亡问题请见邓小平，反而是邓小平要王震和邓力群去看他，跟他们说：“你们（指政研室）的事情，以后再说，反正有用的人，总还是要用就是了。”这又是一个奇闻。与邓小平的一次会面如此一改再改。真是越改越乱。<br />
<br />
“自述”于此再一次改变了以前的说法，明确声称邓小平当时会见的是王震和邓 力群，大概他们也已明白了邓、王二位当时还不能称“中央负责同志”。这样编年谱是露马脚的。但是，为了进一步证明会见时没有于光远、进一步抹掉于光远当时在座的痕迹，“自述”一不做二不休，索性把摘自于光远当场的记录、收进《邓小平文选》第二卷的两篇短文说成系邓力群“回来后整理出来的”，并进而自我吹嘘说，此二文“实际上为‘真理标准’的讨论垫了个底。”<br />
<br />
顺便还提个疑问，邓 力群在“自述”中说他“相信邓（小平）还会出来，等他出来后，有我们这么个单位能帮他工作”。按这个说法，邓力群是应该坚守在政研室的。还在邓小平谈话的两个多月前，我们已经听到传达，邓 力群调国务院财贸小组，我还保存着听传达的记录；邓小平谈话后没多久，邓 力群就去国务院财贸小组任副组长（组长姚依林），而且据他自己说，是他自己答应姚依林调去的。这就令人不解了，邓小平谈话尚余音绕梁，邓力群自己却调走了，他那“等他（邓小平）出来，有我们这么个单位能帮他（邓小平）工作”的“信心”从何谈起呢？<br />
<br />
历史无从修订，事实难以掩盖。这是无法抹掉的常识，也是做人的起码品德。信笔涂抹，自取其辱。从这个角度来说，邓 力群的“自述”，效果无非如此。<br />
<br />
 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;<br />
<br />
五问邓 力群：<br />
<br />
《论总纲》（即《论全党全国各项工作的总纲》）的起草者究竟是谁？<br />
<br />
邓 力群把此文收入《邓 力群文集》（1998 年12 月,当代中国出版社出版）,列为第一篇，不加任何说明，意思是说，《论总纲》是他邓力群的个人作品。<br />
<br />
是这样的么？<br />
<br />
《论总钢》是政研室成立后集体撰写的第一篇文章；撰写此文，是胡乔木提出、经邓小平同意布置给政研室的任务。<br />
<br />
邓小平第一次复出后，大力整顿各项工作。为了扭转“四人帮”“阶级斗争为纲”、不利于国民经济发展和改善人民生活的局面，他概括毛泽东近年的讲话，提出以毛泽东的三项指示（学习无产阶级专政理论，以安定团结为好，把国民经济搞上去）为纲。三项指示统一起来就是全党全国各项工作的总纲。<br />
<br />
胡乔木非常赞成这一指导思想，积极组织撰写文章加以论述。他把这篇文章的写作任务布置给邓 力群，由邓 力群负责找了几个人一起来写。据我所知，参加过写作的人员有：胡绩伟、余宗彦、苏沛、滕文生、龚育之、吴冷西等。由此可见，这篇文章是集体写作的成果。不能说是某一个人的作品，当然也不是邓力群的作品，《邓 力群文集》不应收入这篇文章。<br />
<br />
何以为证？首先请看“自述”的说法。<br />
<br />
第一章，1976年2月邓 力群给华国锋的信中说：“我和四个帮我写文章的同志，商定了一个写文章的架子。五个人分成两个组，进行流水作业。”仅仅隔了一页，“自述”又是另一个说法：“实际是，开头商定文章的框架后，他们四人先分头各写一段”。究竟此文是五个人分两组轮流作业还是四个人分头各写一段，姑置不论。但无论写作过程如何，分工又如何，除邓力群而外还有几个人参加了写作则是清清楚楚的。<br />
<br />
其次，“自述”接着说：“现在有人说胡绩伟是《论总纲》的主要执笔人。不符合事实。”“有人”显然指我。我确实说过（在国内报刊上是第一次指出）《论总纲》是胡绩伟主持下写作的（我不是说胡“主要执笔”），而这正是符合事实的提法。<br />
<br />
我为什么这样提？这需要一点说明：《论总纲》一文曾经数易其稿。有第一稿，第二稿，被胡乔木否定之后还写了笫三稿。由于“批邓，反击右倾翻案风”运动开始，三稿都没有发表。<br />
<br />
第一稿是由胡绩伟主持、余宗彦等几人参加起草的。10月上旬写出后上交胡乔木，胡认为此稿批判“四人帮”的笔锋过于尖锐，篇幅也过长，不能用。胡乔木在约胡绩伟和龚育之谈话时曾评论说：文章不应当用批判和论战的语调来写，要正面论述，从理论原则上讲清道理。还特别对胡绩伟说：“你在报社工作了那么多年，这样长（一万多字）的文章报纸能登吗？写一篇两三千字的社论就行了”（据胡绩伟提供的材料第5页）。<br />
<br />
胡乔木对第一稿文章太长的这一批评，是重要的旁证：“你在报社工作了那么多年”。这句话当然只能是针对长期在《人民日报》工作并曾担任副总编辑的胡绩伟，绝不可能用来指邓力群――邓 力群根本没有在报社工作多年的经历。胡乔木的这句话清楚地表明：这份长达一万多字、不能在报纸上刊登，内容又嫌过于尖锐的文稿，不是邓 力群写的。它只能是胡绩伟主持下集体写作的。难道胡乔木的话还不足为据，还需要论证吗？<br />
<br />
胡绩伟主持写作的头一稿未能通过，这才有第二稿。第二稿倒是邓 力群主持下写成的，但10月中旬拿出的第二稿胡乔木认为仍然不能用。因为虽然去掉了第一稿中的尖锐言词，却改进不大，反而把题目改成了《为巩固无产阶级专政而奋斗》。胡乔木一看就很不满意，说题目就不行，“巩固无产阶级专政”，让人看了，好象我们无产阶级专政不巩固似的。<br />
<br />
这时，胡乔木可能觉得再让邓 力群主持此事难以完成，就把写这篇文章的事交给胡绩伟、龚育之。但是因为当时已经准备让胡绩伟和龚育之去参加西四院（在中南海内）毛选五卷的编辑工作，所以只要他们拟了一个提纲，没有写成稿。于是，胡乔木又让吴冷西另起炉灶。（以上材料来源于当年国务院政研室负责人之一于光远1990年代写的回忆：《论总纲》）<br />
<br />
邓小平二次复出，二次打倒；这段时间“四人帮”“批邓、反击右倾翻案风”，其中大批特批的“三棵大毒草”之一便是《论总纲》，这是大家都知道的。我们起初听说，所批判的是第二稿。近两年根据胡绩伟和于光远的回忆，才有资料证明，被批判的不是第二稿而是第一稿，亦即胡绩伟主持写的《论全党全国各项工作的总纲》。这一稿批判“四人帮”，笔锋特别尖锐，胡乔木看了很不满意。“四人帮”却恰恰因此认为它更具“可批判性”，拿它做文章。<br />
<br />
综合以上种种，不妨稍作归纳：第一，即令是像“自述”所述，邓 力群确实主持了第二稿的写作。但据“自述”所言，此稿写作过程中共有其他四人参与讨论框架，分头写出初稿，以后又参加讨论。请问：这四位参与者付出的劳动、提供的思想资源都不算数了吗？那些主持和参加写第一稿者的劳动也不算数了吗？就算第二稿主持修改和定稿的是你；将题目《为巩固无产阶级专政而奋斗》改成第一稿的《论全党全国各项工作的总纲》的也是你，就能说明这篇文章是你邓力群一个人写的、是你个人的文章吗？<br />
<br />
第二，如果说集体写作和独自写作、主持定稿和全文自始至终全部出自个人之手大有区别，则《为巩固无产阶级专政而奋斗》与《论全党全国各项工作的总纲》就尤其难以混淆。胡乔木交给政治研究室写的文章，前后两稿，两稿都不为胡乔木所取，“四人帮”却偏偏看中了《论全党全国各项工作的总纲》，把它列入“三颗大毒草”大肆批判，《论总纲》于是尤其在中国现代史上留下浓重的一笔。<br />
<br />
特别是邓小平再次复出，看到“四人帮”拿来批判的《论总纲》时（此文从未正式发表），特别加以赞扬说“是香花”，邓力群就将集体的智慧和功劳据为己有。一方面把此文列为他本人文集的头一篇，一方面又在“自述”中宣称“有人说胡绩伟是《论总纲》的主要执笔人，不符合事实”，互为补充。这样看来，他不仅要把他组织撰写的文章据为己有，就连别人主持撰写的文章也要据为己有，岂不令人哑然？<br />
<br />
我在2000年发表于《百年潮》第三期的《在国务院政治研究室的日子》一文中，对邓 力群在“批邓，反击右倾翻案风”运动中没有上推下卸，承担了写作的责任予以肯定，同时也揭露、批评他将《论总纲》当成自己个人的作品收进《邓力群文集》，放在第一篇，“这种作法就不大好”。现在看来，我当时的批评是过于客气了。<br />
<br />
