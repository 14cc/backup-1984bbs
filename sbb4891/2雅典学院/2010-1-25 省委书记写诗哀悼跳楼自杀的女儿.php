　　她以为：<br />
　　世界上弥漫着芬芳，充满着仁爱。<br />
　　（三）<br />
　　她看到爸爸陪毛主席渡江，<br />
　　她也学了游泳，<br />
　　并且学得不错。<br />
　　她和爸爸横渡东湖――从听涛酒家游到磨山<br />
　　爸爸担心她斗不过水浪，<br />
　　她说：“我不怕。”<br />
　　（四）<br />
　　长大了，<br />
　　入世了。<br />
　　为着婚姻问题，<br />
　　一挫，<br />
　　再挫，<br />
　　三挫。<br />
　　她开始认识了：<br />
　　在金钱挂帅的世界里，<br />
　　“爱情”，美妙的名词，<br />
　　不过是权位、金钱与肉身的交易，<br />
　　为买主构造动物的生活，<br />
　　她尝到了苦涩的凄凉。<br />
　　（五）<br />
　　她工作努力，<br />
　　也有一定的才干。<br />
　　她踏踏实实地做，<br />
　　她跟着时间往前赶，<br />
　　她要用自己的努力，<br />
　　开辟一个美好的前程。<br />
　　可是她失望了，<br />
　　不少的与她同辈的人，<br />
　　跑得特快，飞得特高，<br />
　　飞黄腾达了。<br />
　　（六）<br />
　　啊……!<br />
　　原来他们有得天独厚的条件――<br />
　　一位拥有权钱统一体的长辈。<br />
　　他们月月升级，岁岁提位。<br />
　　他们，位高，权重，风光，发财。<br />
　　（七）<br />
　　女儿回看了自己，<br />
　　老在后头赶不上末班车<br />
　　她迷惘了，<br />
　　世界是这样的?<br />
　　真是这样的?<br />
　　怎能是这样的?<br />
　　（八）<br />
　　在她心神恍惚的时候，<br />
　　魔鬼向她抛出了黑线。<br />
　　“跟我走，升官发财!”<br />
　　女儿想：<br />
　　“众人都醉，我何必拒酒。”<br />
