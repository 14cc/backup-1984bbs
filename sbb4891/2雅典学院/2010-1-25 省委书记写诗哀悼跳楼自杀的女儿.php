																											<div id="postmessage_452396" class="t_msgfont">李尔重：论社会主义的本质――对谢韬的批判 <br />
<br />
作者：李尔重 来源：乌有之乡 时间：2009-12-31<br />
<br />
<br />
<br />
李尔重<br />
<br />
2007-4-13<br />
<br />
《炎黄春秋》上谢韬写了一个长篇文章《民主社会主义模式与中国前途》。他以福利国家为民主社会主义的典范，把美帝霸权主义、英国老牌资本主义国家和欧洲一群资本主义国家都称之为民主社会主义国家，指出历史的发展规律是“社会财富分配的不均等是调动社会成员积极性、推动社会进步的杠杆”。他肯定“和平长入社会主义”是马克思主义。<br />
<br />
谢韬的“论民主社会主义”是当今阶级斗争的一个重要反映，比之“西山会议”派来得更恶劣。他要为修正主义树起新时期的黑旗，为复辟制造理论基础。<br />
<br />
这是明目张胆地向马克思主义、向全世界无产阶级、向一切求解放的民族和人民的挑战。马克思主义者应起而应战。应设法组织说理的批判，请着力组织，造成一个反修声势。<br />
<br />
谢韬是卑鄙无耻、良知泯灭的人，一望即知。谢韬断章取义歪曲了马克思、恩格斯、列宁、毛泽东的语言，还是有欺骗性的。<br />
<br />
希望你们花力气查阅谢韬引用的材料，逐一析出其欺骗伎俩，写出一篇“斥民主社会主义”的文章。这是人民的需要，也是当前斗争的需要。<br />
<br />
南京大学教授奚兆永的批判文章，写得很好，有理有据，引经据典，有说服力。“和平长入”之论，已驳得体无完肤。此文可以广为传播。<br />
<br />
尔重<br />
<br />
2007.4.1<br />
<br />
论社会主义的本质 （评民主社会主义）<br />
<br />
李尔重<br />
<br />
（一）<br />
<br />
开宗明义，谢韬有一个发明：“社会主义与资本主义的关系，是继承与发展的关系，而不是推翻和消灭的关系”。“发达资本主义的西方国家，都已成为新资本主义，不同程度地民主社会主义化了。”<br />
<br />
既然不消灭和推翻资本主义，就民主社会主义化了，当然，不须暴力革命，就可以长出民主社会主义――新资本主义。这就是谢韬理论的核心。<br />
<br />
（二）<br />
<br />
在一伙人一天天咒骂和否认阶级斗争时，不但西山会议派向人民及人民信仰的共产主义、政党、政府、社会主义制度，挥动了斧钺、杀声震天；唯恐十七大不抛弃马克思主义和社会主义道路。现在，又来了一队人马，挥动民主社会主义的旗幡，大动杀伐。善良的人们哪能说这不是惨烈的阶级斗争。谢韬认为“左”派理论大回潮是因为“21年来在意识形态上我们妥协、退让的必然结果。”谢韬破釜沉舟地杀起来了。<br />
<br />
谢韬比西山会议派的要“台湾模式”，要共产党交出“兵权”的直言不讳，玩了个新花样，举起了“民主社会主义救中国”的引魂幡，并尽最大努力找出“理论”根据，肯定列宁、斯大林、毛泽东是真正的修正主义分子，而谢韬是正统的马克思主义者。<br />
<br />
谢文从“二战”后的世界形势分析起，开宗明义“以瑞典为代表的民主社会主义制度”，说是世界上的民主社会主义与苏联式的社会主义，还有资本主义，三者展开了竞争，结果是：“民主社会主义胜利，既演变了资本主义，又演变了共产主义，民主社会主义正在改变世界”。认定了：“社会主义与资本主义的关系是继承与发展的关系。”认定了“英国、法国、韩国、瑞典等十三个国家是社会民主党或工党执政……欧洲洪潮涌动”。认定了社会民主党的贡献是：“代表先进生产力的发展需求，化解了工人阶级与资产阶级不共戴天的仇恨，化解了社会主义制度与资本主义制度不共戴天的仇恨，社会主义运动成为和平的、理智的进化过程”。认定了：“发达资本主义的西方国家，都已成为新资本主义，不同程度地民主社会主义化了”。“英国工党首相布莱尔和美国总统克林顿为代表提出的，‘第三条道路’是修订版的民主社会主义。”“民主社会主义把美国赤化了”。亲爱的读者们，你们听清楚了吧？不用解释了吧？<br />
<br />
谢韬振振有词，正好画清自己的嘴脸：民主社会主义者“新资本主义”也、“资本主义之继承与发展”也。美国霸权主义、老牌的英帝国主义、以及在垄断财团统治下的资本主义，都是民主社会主义家族的兄弟，谢韬自己也无法摆脱自己的身份――资本主义的忠实代言人。<br />
<br />
（三）<br />
<br />
修正主义分子总要择取马克思主义的片断，加以歪曲，增加自己画皮上的“马克思主义”色彩，欺骗人民。奚兆永教授和其他同志已把谢韬篡政的引文，逐一地揭穿了。应把这些材料印成册子，广为散发，擦亮人们的眼睛。谢韬引用恩格斯所谓“最后遗嘱”（法兰西阶级斗争导言），得意忘形地给列宁、斯大林、毛泽东扣上了修正主义帽子，把谢韬一族封为“正统马克思主义者”。<br />
<br />
这个手法很卑鄙，对于没有读过“法兰西阶级斗争导言”和有关文件的人很有欺骗作用。<br />
<br />
奚兆永教授对此作了彻底揭发，摘录如下：<br />
<br />
谢韬所引恩格斯的话：“历史表明我们也曾经错了，我们当时所持的观点只是一个幻想，历史做得还要更多；它不仅消除了我们当时的迷误，并且还完全改变了无产阶级进行斗争的条件。一八四八年的斗争方法（引者注：指《共产党宣言》中说的暴力革命），今天在一些方面都已经陈旧了。这一点是值得在这里较仔细地加以研究的。历史清楚地表明，当时欧洲大陆经济发展的状况还远没有成熟到可以铲除资本主义的程度……。在1848年要以一次简单的突袭来达到社会改造，是多么不可能的事情。旧式的起义，在一八四八年以前到处都起决定作用的筑垒的巷战，现在大都陈旧了。如果说在国家之间进行战争的条件已经起了变化，那么阶级斗争的条件也同样起了变化。实行突然袭击的时代，由自觉地少数人带领着不自觉的群众实现革命的时代已经过去了。无产阶级的一种崭新的斗争方式就开始被采用，并并且迅速获得进一步的发展。原来，在资产阶级借以组织其统治的国家机构中，也有许多东西是工人阶级可能利用来对这些机构本身进行斗争的。工人开始参加联邦议会、市镇委员会以及工商仲裁法庭的选举；他们开始同资产阶级争夺每一个由选举产生的职位，只要在职位找人时有足够的工人票数参加表决。结果，资产阶级和政府害怕工人政党的合法活动甚于害怕它的不合法活动，害怕选举成就甚于害怕起义的成就。在罗曼语国家里，人们也开始愈益了解到对旧策略必须加以修改了。德国所做出的利用选举权夺取我们所能夺得的一切阵地的榜样，到处都有人模仿。无准备的攻击，到处都退到次要的地位上去了。”<br />
<br />
这就是谢韬想借用的把列宁、斯大林、毛泽东打入修正主义者的一根棍子。<br />
<br />
实际上这是恩格斯调整斗争策略的意见，要工人阶级不要只注重非法的斗争，也要运用合法的斗争，丝毫也没有资本主义可以和平步入社会主义而不需暴力革命的话。<br />
<br />
奚兆永教授引用了恩格斯多种其他文件，阐明了这一点。<br />
<br />
谢韬引用的《法兰西阶级斗争导言》是1895年3月6日恩格斯的文章，被称为“最后遗言”，借以唬人。奚兆永教授除了引用了恩格思多种其他文件以证明恩格斯绝对没有丢掉暴力斗争，“和平步入社会主义”的主张，特别引用了三个极其重要的文件――1885年3月8日写给德国社会民主党执行委员会书记理查&#8226;费舍的一封信；1895年3月25日写给卡尔&#8226;考茨基的一封信和1895年4月1日写给卡尔&#8226;考茨基的一封信。这是三个文件，把“最后遗嘱”的谎骗完全揭得体无完肤了。<br />
<br />
1895年3月8日给费舍的信上写有这样的话：“既然我不能容忍你们忠于绝对守法，任何情况下都守法，甚至对那些已被其编制者违反的法律也要守法……简言之，即忠于右脸挨了耳光再把左脸送过去的政策。”“我认为，如果你们宣扬绝对放弃暴力行为，是决捞不到一点好处的。没有人会相信这一点好处的。没有人会相信这一点，也没有一个国家的任何一个政党会走得这么远。竟然放弃拿起武器对抗不发行为这一权利。”<br />
<br />
1895年3月25日寄给卡尔&#8226;考茨基的信再次批评费舍为“不坚定”者，“害怕防止政变法案”，害怕暴力革命行动。<br />
<br />
1895年4月1日寄给卡尔考茨基的信上说：“使我惊惊讶的是，今天我发现，《前进报》事先不通知我就先发表我的《导言》摘录。在这篇经过修饰整理的摘录中，我是以一个爱好和平的，无论如何要守法的崇拜者出现的。我特别希望《导言》能全文发表在《前进报》上，以消除这个可耻现象。”<br />
<br />
不须注释了：恩格斯于1895年3月6日《导言》之后的文章，已经把“否定暴力革命”之歪曲邪说。都明白地扫光了。谢韬也不必把修正主义帽子送别人了，留着自己戴吧。<br />
<br />
(四)<br />
<br />
从马克思主义出现之后，关于社会主义的本质，无产阶级是否要武装夺取政权，是否要无产阶级专政和在专政下继续革命，一直是争论不休的问题。阶级、阶级残余、旧阶级的意识形态传统还存在的时候，这一争端不会结束，旧统治阶级的复辟企图是不会消亡的，总要寻机兴风作浪的。<br />
<br />
首先是关于社会主义的本质问题。<br />
<br />
“社会主义”一词，不是马克思的创造，是十八世纪资产阶级民主革命运动的产物。1848年《共产党宣言》出现之前，不但有空想的社会主义，而且有封建的社会主义，小资产阶级的社会主义，德国的或“真正的”社会主义，资产阶级的社会主义等。马克思与恩格斯对这些做过详细的批判，一言以蔽之，这些都是冒牌的社会主义。恩格斯于1888年英文版《共产党宣言序言》中说：“在1847年，所谓社会主义，一方面是指那些信奉各种空想学说的分子，即英国的欧文派和法国的傅立叶派；这两个流派都已经变成纯粹的宗派，并在逐渐走向灭亡。另一方面是各种各样的社会庸医，他们都答应要用各种补缀办法采消除一切社会腐败而不伤及资本的利润”。“在1847年，社会主义是资产阶级的运动，而共产主义则是工人阶级的运动”。因为“社会主义”一词，已被滥用了，已成为各种流派习用的包装纸，马克思、恩格斯不愿意用它，于是把《宣言》名之曰《共产党宣言》，而不叫《社会主义宣言》。恩格斯在《人民国家报》国际问题论集的序言里更明确地把自己与社会民主主义者区别开，自认为是共产主义者：“读者将会看到，在所有的这些文章里，尤其是在最后这篇文章里，我处处不把自己称为社会民主主义者．而称作共产主义者，这是因为当时在各个国家里，那种根本不把全部生产资料转归社会所有的口号写在自己的旗帜上的人，自称是社会民主主义者。”<br />
<br />
由此可见马克思、恩格斯心目中社会主义，就是共产主义，而不是别的。<br />
<br />
什么是共产主义?在《共产党宣言》里给了一个简明的概括：“共产党可以用一句话把自己的理论概括起来，消灭私有制”。“共产主义的特征不是要废除一般的私有制，而是要废除资产阶级的所有制。<br />
<br />
谈社会主义而不谈废除私有制，在保存资本主义生产方式下做些改良，便说可以进入社会主义，是历代修正主义的特征，也是各种改良主义者的特征。<br />
<br />
只要私人占有社会的生产资料，那里会有和谐、公平和民主？<br />
<br />
社会主义的本意就是共产主义，避开共产主义，避开废除私有制的所谓“社会主义”，都是赝品。谢韬用多少花言巧语，也盖不住狐狸尾巴。<br />
<br />
小平同志说得明白：“社会主义的目的就是要全国人民共同富裕，不是两极分化，如果我们的政策导致两极分化，我们就失败了；如果产生了什么新的资产阶级，那我们就真走了邪路了”。(邓小平文选第三卷第110－111页)<br />
<br />
不除私有制怎能不有资产阶级，怎能不出两极分化?<br />
<br />
民主社会主义者，“新资本主义”也，“资本主义的继承与发展”也。谢韬说了实话。<br />
<br />
（五）<br />
<br />
废除私有制，就挖掉了资产阶级存在的根本，资产阶级当然要反抗，无产阶级要解放，自然地产生了暴力革命。<br />
<br />
无产阶级并不愿使用暴力，如果资产阶级自动退出历史舞台，自愿把占有的生产资料用于社会，和平过渡当然好，可惜历史上没有过这种先例。武装到牙齿的垄断财团自动让位是不可能的。<br />
<br />
中国的民族资产阶级是在和平改造过程中让出私有生产资料的。这是因为有历史的特殊条件：无产阶级率领人民打败了官僚买办阶级并没收了它的财产，有了自己的巩固政权和强大的武装，迫得民族资产阶级不得不缴枪。如果任何一个别的国家也有了这样的局势，也可以和平过渡。但这仍是以暴力为基础的和平过渡。<br />
<br />
保存私有制，不改变资本主义生产方式，无产阶级及众多人民的被压迫、被剥消的地位不变，这是假社会主义的所谓过渡，即所谓民主社会主义。<br />
<br />
资本主义前期对殖民地、半殖民地的侵略，采用的炮舰政策，所到之处，以武装侵略形式出现。这种政策容易激起民族的强烈反抗。帝国主义学乖了，尤其是二战后，帝国主义对第三世界采取了政治、经济、武装、文化的全面侵略。在政治上极力在各国内部培育民族内奸，挑动各国内部矛盾，在经济上大力搞资本输出，抢占市场，抢夺资源，搞掠夺性开发，破坏人民的生存条件；在军事上，一方面搞武力直接侵略，一方面到处安军事基地，威胁世界；在文化上，大量输出个人主义，自私自利、色情、腐败随落的文化，腐蚀世界人民的灵魂，瓦解其自求解放的战斗精神。这一套毒辣行径，便是所谓新自由主义。<br />
<br />
新自由主义做为修正主义的灵魂，在无产阶级队伍中制造幻想，便是民主社会主义，便是资本主义和平步入社会主义的邪说。这是社会民主党的版权，并不是谢韬的发明。<br />
<br />
（六）<br />
<br />
暴力革命才能夺取政权。一部阶级斗争史都是这样写的。并不是无产阶级单有的创造。<br />
<br />
掌握政权是为了统绐，一个阶级专政压迫别一个阶级，这又是阶级斗争史写明了的。无产阶级专政并不是无产阶级的创造。<br />
<br />
专政与民主是矛盾的统一：被专政的阶级，是没有自由的，而专政阶级的内部却有其自己的民主。秦始皇专制，也听从李斯的“谏逐客会”，希特勒专制，也听从其僚属的谏议。垄断财团专治，也有资产阶级内的民主。<br />
<br />
无产阶级为了巩固自己的政权，当然要对破坏社会主义的反动派实行专政，对人民则给予广泛的民主。这就是宪法上写的“人民民主专政(即无产阶级专政)”。人民民主专政促进社会主义全面建设发展，同时制止反动派的破坏，没有这个专政，社会主义建设与发展，通向共产主义，是不可能的。<br />
<br />
人民民主专政是遂行无产阶级共产主义路线的手段。<br />
<br />
谢韬的民主社会主义的民主是为了“混合私有制、社会市场经济、福利保障制度”服务的，根本与社会主义的目的――共产主义不沾边，牢固地保护了私有制。这是共产党和所领导的人民坚决反对的。<br />
<br />
谢韬尽力吹捧瑞典的“民主社会主义”，贬抑社会主义（共产主义）国家为“劫富济贫”，不能致人民于幸福。列宁时代曾在苏联创造了发达资本主义国家所不能企及的繁荣和幸福：世界上第一个消灭了失业的国家，第一个实现八小时工作制的国家，第一个全民免费教育免费医疗的国家，第一个向全民提供免费住宅的国家，第一个实现男女同工同酬的国家。到了斯大林时代，成为反法西斯主力的国家。而这些都是在破败而落后的条件下创造出来的。<br />
<br />
谢韬极力夸赞1978年后，中国改革开放的发展，说是现在的劳动生产率比1978年增长了33倍。而1949年到1976年则是以每年13.6％的生产指数增长，1976年比1949年劳动生产率该增长了多少倍?一个初步的比较完整的工业体系，两弹一星，都是那时在破败落后的条件下创造出来的。<br />
<br />
历史证明了：人民民主专政涣发了人民的积极性和创造性，涣发了人民对缔造共产主义的不绝贡献，而绝不是谢韬的“混合私有制、社会市场经济、福利保障制度”所能办到的。<br />
<br />
（七）<br />
<br />
谢韬极力反对阶级斗争，而阶级斗争是阶级社会的客观存在，谁也没有办法消除雇主与被雇用者之间的矛盾，资产阶级代言人与工人阶级的矛盾，正如不能消除谢韬的修正主义与马克思主义之矛庐。<br />
<br />
前面已曾说过：无产阶级专政的目的，是为遂行工人阶级路线，通向共产主义。<br />
<br />
在这条前进的道路上，并不是通畅无阻的，这是充满着人民内部矛盾与敌我之间的矛盾夕<br />
<br />
在社会主义发展中， 我们与资产阶级的矛盾，本质上是对抗性的矛盾，另外，也有非时抗的一面。在资产阶级统治时，它不会自动退出历史舞台，你不打，它不会倒；无产阶级把它打倒，实现无产阶级专政后，资产阶级及一切反动势的残余，结合国际资产阶级的力量，总不会放弃复辞的企图厂搞多种多样的捣乱，组织武苯叛乱，组织秘密破坏，在意识形态领域千方百计用腐朽、颓废、淫乱、堕落的意识形态向人民进攻。国内外的阶级敌人都懂得意识形态的进攻，既便宜又收效大，可以不用大炮从内部攻破对方的堡垒。苏联解体、东欧剧变，完全证明了这一点。<br />
<br />
人民内部矛盾包含公与私的矛盾，先进与落后的矛盾。小生产的汪洋大海，极深厚地保留私有观念和习惯力量，很难一次根绝。这些矛盾，只能在党的领导，运用批评自我批评的方式，人民自我教育提高。但人民中的落后的习惯势力又常常是敌对思想进攻的跳板，阶级斗争的影子便常常反映到人民内部矛盾里。根除这种习惯势力是极艰苦的，不经过长期的反复教育，不可能收到完满的效果，《共产党宣言》指示我们“要在发展中与传统观念彻底绝裂”。<br />
<br />
不论敌我矛盾或人民内部矛盾，都不是抽象的存在，它们常存在于政治、经济、军事、文化的各个具体角落中，或者说存在于文化<br />
<br />
这就是通向共产主义路上的重要障碍；无产阶级专政为着逐行阶级路线，就要批荆斩赖，开拓开进。这就是敌我矛盾中的阶级斗争，或反映在人民思想内的阶级斗争。在当前世界共产主义运动中，便是马克斯主义与修正主义的斗争。谢韬高举民主社会主义的破旗向马克思主义大举进攻，正表示了所谓“社会各阶级团结起来”、“劳资双赢”的破产。<br />
<br />
无产阶级专政下的这场艰巨的斗争，即《共产党宣言》指示的两个彻底决裂：“共产主义革命就是同传统所有制关系，实行最彻底的决裂。在它自己的发展进程中，同传统的观念实行最彻底的决裂”，也就是毛主席说的“文化革命”、防止糖衣炮弹的革命。<br />
<br />
1949年进入社会主义革命阶段后，党领导带我们进行了三大改造，三反五反、反右派、反左倾，评武训传、评海瑞罢官、合作化运动，评四大自由、搞“四清”运动。这一系列的斗争，都是为两个彻底决裂服务的，是深刻的文化革命。<br />
<br />
以赫鲁晓夫为首的修正主义在苏联急剧的发展，把一个社会主义国家变成了社会帝国主义。毛主席担心中国的前途，下决心搞“四清”，他曾认为只要把基层党搞好了，中国就不会变修。“四清”搞到第三个年头，他认为病在头不在脚，“党内有个走资派”，发动了文化大革命，他希望筑起一道反修防修的万里长城。结果党内的走资派林彪、“四人帮”乘机捣乱，把一场文化大革命弄成“全面内战，打倒一切”的局面。毛主席病弱不堪，在尼克松面前都说：“我快不行了”，无力使“文革”正常进行。他老人家左右手周恩来、朱德先走了，他也去世了。“文革”并未达到预期的目的，使党受到了很大的损伤。<br />
<br />
但是，“文革”本身是无产专政下继续革命，暴露了党内确存走资派，大方向没有错，泼洗孩子的脏水，不要连孩子一起扔掉。<br />
<br />
文化革命是无产阶级专政下的继续革命，是实现“两个彻底决裂”之必需，是无法避开的阶级斗争。<br />
<br />
（八）<br />
<br />
马克思主义的社会主义，是以共产主义为内涵，以共产主义为目标的，绝不同于谢韬的以“新资本主义”为内涵的伪社会主义。马克思主义的社会主义与暴力革命，无产阶级专政、无产阶级专政上继续革命是一个统―整体，缺一不可。新老修正主义的共同点在于：否定社会主义的本质――共产主义，同时，否定为了实现共产主义的三个保障――暴力革命、无产阶级专政，无产阶级专政下继续革命。谢韬及其前辈，在这块试金石面前都不能不现出原形――反马克思主义的修正主义。<br />
<br />
这场阶级斗争还在发展，树欲静而风不止，我们只有迎风而上。谢韬不许我们退让，我们更要掌握批判斗争的权利。胡锦涛同志说得对：“一段时期以来，境外的敌对势力和媒体，大肆攻击我们国家领导人和政治制度，而国内媒体打着政治体制改革的旗号，宣传西方资产阶级议会民主、人权、新闻自由，散布资产阶级自由化的观点，针对这种错误，决不能手软，要加强新闻舆论的管理。”(2004年胡锦涛对宣传工作的批示)<br />
<br />
和平演变，不废一枪一弹，搞垮了一个社会主义阵营：苏联解体，东欧剧变；资产阶级自由化，搞垮了我们两位总书记，酿成了“六&#8226;四”事件。无硝烟的攻击多么厉害呀？这里有海样的痛苦教训，如果做出正确的总结，也可以收到巨大的效益。可惜的是：几次党代表大会对这样大的政治事件，没有做出批判检查。<br />
<br />
2007年4月24日</div>

							
							
							
							
