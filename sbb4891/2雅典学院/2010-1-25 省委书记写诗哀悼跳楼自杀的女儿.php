　　晓丹自杀后，李尔重写了“晓丹之死”的长诗怀念女儿。<br />
　　此资料系本博秦全耀根据丑牛先生作品缩减而成。<br />
　　<br />
晓丹之死<br />
　　李尔重<br />
<br />
　　“这不仅是我和我家庭的悲剧，也是当今社会和国家的悲剧。整整一代人都被资本的浪潮所淹没。晓丹害的是抑郁症，不是生理上的病，而是社会压抑的病。她已经在物质上、精神上都处于绝望和崩溃的境地。对我当然是一次沉重的打击。<br />
　　我们流血牺牲，创立了一个新社会，这个社会慢慢地变成今天这个样子，又向我们反扑过来。有些共产党人，当了俘虏，成了牺牲品，我们的子女像晓丹，也成了牺牲品。我决不会倒下来，而且要予以更大的反击。<br />
　　我的可爱又可怜的女儿生于1948年5月，殁于2005年（乙酉）7月2日5时，享年57岁。她生在牡丹江，取名小丹，后改为晓丹。初生时，母无奶，得助于中、日十余人母奶。移哈尔滨时，她一岁多可以吃稀饭，便以玉米糊及稀粥为食。她大便干结，无钱买水果，便用泻药通便。1949年到武汉仍然如此喂养。她很瘦弱，我有时到野外打鸟为她增加营养。五十年代发了薪水，我才给她买奶粉吃。从此，她吃“糖水”长大成人了。她在父母舍生忘死创造了的“新社会”中，被恶风邪气抢走了。<br />
