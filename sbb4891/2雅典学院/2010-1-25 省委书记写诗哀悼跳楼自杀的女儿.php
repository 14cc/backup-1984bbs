　　<br />
　　<br />
李尔重，2009年12月26日6时38分在武汉逝世，享年96岁。<br />
<br />
　　李尔重生前曾任中共陕西省委常务书记，中共河北省委书记兼省长。中共湖北省顾委会副主任。他被毛泽东主席誉为“我们的作家和才子”，先后创作了《扬子江边》、《新战争与和平》等著作。<br />
　　晓丹，李尔重的爱女。任中共湖北省委办公厅老干处副处长。她没在办公室坐班，主要是作为李老的生活秘书，照料她的父亲。不少媒体报道老省长们的家事时，说尔重当了好几个省的一把手，子女都是平民，唯一沾他光的是女儿晓丹，下岗之后，当了他的生活秘书，给了个副处级待遇。但谁也不报道，老省长家就这么一个芝麻小官，却于2005年7月2日跳楼身亡。<br />
　　晓丹自杀前，曾算过一笔帐：“在这个省级干部住宅大院内，可能要数我爸爸的官职最高，也可能算是最穷的了。我管这个家，真难。李老每月四千元左右，我妈长期住院，请两个人轮流陪护，一个人800元。这一笔是一千六百元，家里总得请一个做饭的人，每月最少也得六百元，二千二百元，还有一千八百元，七口人吃饭，最低也得平均每人二百元，这得一千四百元，剩下四百元每月水电和其他杂用，够吗？逢到开学，李老的侄孙要上学，得要寄一些钱去，他亲弟弟七老八十了，还要种田顾一家人生活，没有钱让孙子上学……<br />
