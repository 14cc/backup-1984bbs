													<h2>7・5悲剧：选择理性反思还是继续煽动？</h2>
						
						
																											<div id="postmessage_731297" class="t_msgfont">我是一个在新疆出生、长大的汉人，新疆是我永远的故乡。然而，我同时也知道，那其实也是永远的异乡异土。<br />
<br />
现在新疆的汉人，向上数三代，有几个还是新疆本地人？多说一点，有千分之一、万分之一么？维族、哈族在这片土地上生活了上千年，这是他们的祖先留给他们的土地，他们有自己的语言、史诗、音乐、舞蹈和英雄。汉族人对于新疆这片土地而言，只是客人，是移民，如果再说的赤裸裸一些，其实是种族意义上的侵略者……这是公平的讨论这个问题的最基本道德前提。所有的汉人，无论是新疆的还是外省的，如果还尚有良知在胸，都不应当刻意在这一前提面前扭转脸去，闭上眼睛。<br />
<br />
我这么说，不是要鼓动仇杀或分裂，也不是要为7・5中的暴力寻找任何借口或理由，我们应当反对任何形式的血腥暴力，无论暴力打着什么样的旗号。但我同时也坚信，所有能称之为美好幸福的未来，都不可能建立在无知和谎言的基础上。所有族群之间的和睦相处，都必须建立在互相了解、互相尊重前提之下。说到这里，我想问一问广大屏幕面前的汉族同胞们，你们对维族、哈族兄弟们的史诗和英雄了解多少呢？还是相当然的、无意识的认为，他们没有什么历史？在中国当前的语境中，仿佛也只有汉族的历史是具有存在感的，具有被了解、被学习、被传承的正当性。维族人也知道汉唐宋明清，敢问有几个汉人知道《乌古斯传》讲述的是怎样的传奇？历史和文化，是一个民族尊严的载体，没有了解，谈何尊重；没有尊重，谈何平等；没有平等，谈何共处……既然，我们口口声声说他们是我们的兄弟民族，不能只是在勘探出新疆丰富的资源时，才想到他们是兄弟；转眼之间，就可以认为他们是无需被了解的、没有丝毫历史存在感的民族躯壳。而这些，是不是才是7・5这样的悲剧背后的真正原因？<br />
<br />
我的父辈、祖辈们到了新疆，因为内地的兵役或饥荒，新疆广袤的土地接纳了他们，之后，我们降生在新疆清澈的蓝天下。新疆成为上千万我们这一代汉人不可选择的故乡，这已经成为事实。我们承认，我们是外来者，然而数千万的外来者和他们的子孙，已经深深的扎根在这里。无论未来漂泊到哪里，我们的最温暖的梦境，永远是新疆美丽的天山、牧场、清澈的蓝天、湖泊和哪些向四面伸展、绵延到无穷远处的戈壁。<br />
<br />
无论从哪个角度讲，现在的现实是，数千万的汉人已经不可能如当初大举迁入一般再全部退出这片土地了，在未来无尽悠远的岁月里，汉族人必将和维族、哈族等新疆各族共存共处共生。这是新疆未来生活的最基本图景。真正有意义、有价值的讨论，都必须建立在这一图景之中。我们应当考虑的是如何更为融洽的共处，在这片各族共存的土地之上，以尽可能少的冲突和对抗，去完成对彼此的认识、尊重和互动。<br />
<br />
怎样才能尽快的治愈那些由于悲剧而造成的伤害，拂去笼罩在人们心头的阴影，让爱代替恨，这才是当前最值得关注，最有意义的问题。我们都反对信息封锁，但是想想看，那些所谓记录了最真切真相的图片和视频，是不是现在的人群所能够理解和承受的？现在就让全国广泛的人群再次回到那一天的地狱场景中，除了激发更大规模的仇恨甚至民族暴力之外，还能带来什么？那些打着寻求真相的旗号所进行的无原则的煽动，四处散布的消息，仿佛只有再次挑起更惨烈的大规模民族对抗，方才能够对得起广大看客们期盼的眼球和炽热的民族热肠。难道说，一次悲剧的正当收获只能是更多的悲剧？一次流血必须以更多的无辜者的鲜血和生命做注脚？<br />
<br />
还需要述说的事实是，在7・5悲剧中逃生幸存的汉人，有很多是由好心的维族、哈族老人带领着逃离现场的，还有些是在一家维族人开的宾馆地下室中躲藏，由维族老板为他们掩护方才逃过一劫的。这是我听幸存者亲自讲述的事实，然而这样的事情显然没能引起流言家们的注意，比较而言，当然还是血淋淋的屠杀故事，更能满足民族大义者们的需要……<br />
<br />
真相当然需要澄清，然而，那要等到人们能够以正确的方式理解和接受真相时。7・5只是一个纯粹的充满血腥的悲剧，只有当人们真正懂得，认识和了解悲剧不是为了释放自己心头的魔鬼，而是为了避免悲剧重演时，只有当民族之间不再将对方视作不可理喻的异类时，各民族的人们才能够一同来慢慢的回顾这个悲剧，用紧握的手和满含热泪的眼睛，一起为那些在悲剧中丧生的生命祷告，为了过去，更为了未来。<br />
