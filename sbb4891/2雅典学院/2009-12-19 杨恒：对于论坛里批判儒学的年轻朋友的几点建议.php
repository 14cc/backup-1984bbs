<strong>许琳：孔子学院都有专门账户 资金管理没有问题 </strong><br />
<br />
--------------------------------------------------------------------------------<br />
2009年03月12日&nbsp;&nbsp;13:13:40&nbsp; &nbsp; 杭州网&nbsp;&nbsp;<br />
十一届全国人大二次会议新闻中心今天上午举行“孔子学院的发展情况”专题采访，邀请教育部、国家汉办等有关负责人接受中外记者的采访。在谈到资金问题时，国家汉办主任、孔子学院总部总干事许琳表示，大学都有孔子学院的专门账户，也有独立于学校之外的审计机构，在资金管理上，目前是没有问题的。 <br />
许琳说，在《孔子学院章程》里有严格的规定，需要孔子学院由中外双方组成的理事会来决定怎么用钱、用多少钱。从总部来说，也有专门的文件，教育部、财政部共同制定的大概有21个文件，专门是用于怎么管好这笔资金。总的来说，大学都有孔子学院的专门账户，也有独立于学校之外的审计机构，每年对孔子学院的中方资金使用情况进行审计，在孔子学院里面具体每笔开支都需要孔子学院的院长和他们所在的比如说文学院、外事国际处，以及大学的副校长乃至于校长签字才能使用资金。所以，在资金管理上，目前是没有问题的。 <br />
<br />
厦门大学校长朱崇实说，厦门大学已经在10个国家和10所著名大学合作创办了孔子学院。在资金的使用方面，非常地谨慎，比如说和德国的特里尔大学合作建设了特里尔大学孔子学院，我们的每笔经费都要经过孔子学院中方和外方两位院长联署才能支出这笔费用。因此到目前为止，学校是对整个资金的管理和使用感到满意，在某些方面，比学校的财务管得还要严格一点。 <br />
<br />
人民大学校长纪宝成称，去年参加过孔子学院的评估督导组，包括美国和墨西哥等四所孔子学院，在评估督导的过程中，中方资金的使用情况以及整个孔子学院的财务状况，都进行了详细的了解。孔子学院本身也有很详细的报告。“我参加了四所孔子学院的评估督导，给我的印象是，这些资金的使用得很好，不存在什么问题。” <br />
武汉大学校长顾海良介绍，武汉大学和巴黎七大孔子学院建立得比较早一点，已经有4年的历史了，从 2006年正式开班以来，对财务进行共同管理。和其他孔子学院一样，巴黎七大孔子学院也是由巴黎七大的前任校长任院长，武汉大学也派一位教学方面的行政院长。财务是经过校长和副院长共同协商的。每年的年度报表都要送来审核。从现在来看，财务开支还是非常合理的。<br />
