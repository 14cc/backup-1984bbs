													<h2>杨恒：对于论坛里批判儒学的年轻朋友的几点建议</h2>
						
						
																											<div id="postmessage_382131" class="t_msgfont">“己所不欲，匆施于人。己欲立而立人，己欲达而达人。”-------这是谁说的？<br />
“大道之行也，天下为公，选贤任能。”--------这是谁说的？<br />
“民为贵，社稷次之，君为轻。”--------这又是谁说的？<br />
“贼仁者之谓‘贼’，贼义者谓之‘残’，残贼之人谓之一夫。闻诛一夫纣矣，未闻弑君也”----这又是谁说的？<br />
“行一不义，杀一无辜，而得天下，皆不为也”-----这又是谁说的？<br />
<br />
你们反对什么首先要搞清楚你要反的到底是什么东西，你们知道近现代谁对儒家最深恶痛绝欲毁灭而令其消失吗？――他是毛泽东！但是你们知道暴君毛泽东为什么极端痛恨儒家吗？他痛恨儒家的情感和理论根源是什么？好好思考一下吧。今天，让人感到可悲的是，自诩是反独裁的民主人士此刻和大独裁者在对待视仁爱孝悌为根本的儒家有着异常一致的立场，可见大独裁者给人洗脑不是洗了一代呀。<br />
<br />
你们这样吵，关键就是都不读书。全都是连一本儒学的书都没完整读过的人，个个都是破口大骂，让人不知道你们要干什么。你们一定要清楚：秦始皇的暴政，秦始皇要负责，赖不到被他焚书坑儒的儒家身上；毛泽东的暴政，毛泽东要负责，赖不到他要批倒批臭的孔子身上。这种道理还不明白吗？儒家的根本精神是仁爱孝悌。天下一王儒家从未追求过，那是儒家的敌人法家的梦想。儒家的价值取向是：行一不义，杀一不辜，而得天下，皆不为也！一定要搞清中国传统中儒家和法家的区别。毛泽东为什么爱法家而恨儒家呢？因为儒家是暴君的敌人，而法家是暴君的帮凶。儒家实际上是社会伦理学，它没有政治体制的完整设计，不是独裁专制体制的设计者，也不是拥护者。它的理想是大同社会-----大道之行也，天下为公，选贤任能。王道是不得以而求其次的。“仁爱、孝悌”“礼义廉耻”“信义”“节气”那些和人吃人有关系呢？我的意思是不批秦始皇，去批孔孟，是很荒谬的。秦始皇罪恶应该彻底反思，但是把秦始皇的罪恶反省到孔孟身上，是很可笑的，秦始皇的精神老师是李斯、韩非子，而不是孔孟。<br />
<br />
<br />
不敢直面专制暴君，却找个专制暴君的敌人痛骂，是当今中国懦夫的典型特征。另外：那些维护专制体制的孔庆东之流根本就不是什么儒家，那是犬共，连犬儒都算不上。年轻人，把你们的精力放到真正的维护公民权利，启蒙公民意识，建设公民社会的现实问题上。至于中国文化的问题，以你们的学养还远远没有什么有益的见地，现在可以先读书，再思考，5年以后再讨论。<br />
<br />
当今中国之权贵者，面对民主宪政浩浩荡荡的历史必然，因一己之私利，先后不断抛出“经济决定论”、“素质决定论”、“文化决定论”。先说中国太穷搞不成民主，以后经济好了；又说中国老百姓素质差搞不成民主；现在遍地都是大学生了；又开始到处大叫，中国文化太专制所以搞不成民主。不幸的是，一些朋友跟着瞎起哄，把中共不搞民主宪政的原因往孔孟身上推，何其糊涂！我要告诉这些朋友，中国大陆还是个儒家传统的国家吗？中国大陆儒家传统早已经荡然无存了，你们以为孔庆东之流是儒家？他们是赤裸裸的犬共。如果这些朋友要体验一下，儒家传统与民主宪政的关系，可以看看台湾。一个儒家传统深厚的地区，可以直选总统；而一个儒家传统消失殆尽的大陆，我们可以直选村长。看看这一幕幕，我们就知道中国大陆谁是民主宪政的最大障碍，是儒家吗？<br />
<br />
在推动民主宪政的事业上，反儒家却不反法家，无论从战略上还是战术上都是错误的。中共现在尊儒，和以前的皇帝一样，不过是找一件老百姓能够接受的外衣罢了，其实骨子里还是法家的一套，儒表法里。这个大家一定要清楚，知道儒法的根本对立。儒家真谛是仁爱，老共仁爱否？儒家讲伦理，老共伦理否？儒家讲信义，老共信义否？儒家讲天下为公，老共公心否？<br />
<br />
民主自由人权法治，来自西方，无可置疑。我只是想和年轻朋友说：儒家的仁爱孝悌礼义廉耻，不是民主宪政的敌人，搞民主要反专制独裁的政治体制，不是要反传统文化。传统文化可以反思批判，但绝不可应该彻底否定；独裁专制即是独夫民贼要彻底否定，绝不可妥协。很多朋友把战场搞错了。现在要猛烈批判的是那些不顾廉耻暴虐专横，关押刘晓波逼死唐福珍的专制集团，而不是主张仁爱孝悌的孔子孟子。<br />
