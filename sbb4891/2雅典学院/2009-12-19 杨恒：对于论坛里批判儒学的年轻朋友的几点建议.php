													<h2>我n年前的一篇文章了</h2>
						
						
																											<div id="postmessage_382154" class="t_msgfont">我来谈谈儒家的论语<br />
以前有首歌：“你也说聊斋，我也说聊斋……”现在论语啊孔子啊很热闹，我也来凑凑热闹。<br />
 <br />
今天上午贴了个与我观点比较接近的帖子，虽然看的人不多，反正是贴出来了。其实我一直不喜欢仅仅从论语的角度来说老夫子，但既然儒家思想是中国人思维当中最主要的思想之一，说儒家不能不说论语，所以还是从论语里开说吧。<br />
 <br />
论语哪句话最重要？有的说是“仁”，也许对，但不知道仁出现了到底多少次，又没有两个是一样的，所以仁是什么，不知道；有人认为是“有教无类”，这肯定不对，“朽木不可雕也”也是老夫子说的。这两句话是否矛盾，自己看吧。到底白天睡觉的人是教还是不教。<br />
<br />
我读论语，眼睛里扎的最深的一根梁木是“父为子隐，子为父隐”。语出论语第13篇子路: 叶公语孔子曰:“吾党有直躬者,其父攘羊,而子证之。”孔子曰:“吾党之直者异于是:父为子隐,子为父隐,直在其中矣。” <br />
<br />
什么意思？<br />
<br />
当老爸的看到儿子犯罪，不该去告发，而该为儿子隐瞒，能做伪证是更好啦，儿子看到老爸犯法，也不该告发作证，而该为老爸做伪证隐瞒。对老夫子来说，这个就是“直”，正直，正确。<br />
 <br />
老夫子这段话并非说过就算了，到了老人家的好学生跟亲密接班人孟夫子那里，就有了一个完整的版本。<br />
桃应问，“舜为天子，皋陶为士，瞽叟杀人，则如之何？”孟子曰：“执之而已矣。”“然则舜不禁与？”曰：“夫舜恶得而禁之，夫有所受之也。”“然则舜如之何？”曰：“舜视弃天下犹弃敝屣也。窃负而逃，遵海滨而处，终身欣然，乐而忘天下。”（《孟子・尽心上篇》<br />
<br />
什么意思呢？中国历史上最杰出的国家领导人，卓越的智慧超群的舜的老爸要是杀人犯法了怎么办？舜就放弃权力，背着他老爸逃到国外去，一辈子很开心（可能担任领导人期间钱没少挣吧），忘记自己曾经有过的权力。<br />
 <br />
如果说这还是假设的话，到了大宋朝，欧阳修那里，这个理论有了遇到理论联系实践的机会了：皇帝他老爸跟其余个高干的老爸组成“十老爸”黑社会性质组织（“十阿父”），在洛阳为所欲为，横行不法，有关部门把这个事情告诉最高领导人周世宗了，但他不管（有司以闻，世宗不问）。我们的唐宋八大家里日子混的比较舒坦的欧阳修说了，对啊，这个很有道理。<br />
<br />
这三件事情不是孤立的，推而广之，养儿防老的要义也在于此，不孝有三後一句的要义也在于此，而计划生育政策（无论政策本身是非）也部分由此。如果加上孟子“老吾老以及人之老”来发挥一下，到了民国年间，厚黑学给我们画了一个又一个圈圈，给我们指出了一个认识中国历史的新视角，到了史无前例的时候，中国文化彻底颠倒了过来，儿女又开始主动揭发父母的罪状，“划清界限”也是那个时候的发明，从一个极端走到另一个极端，这是中国人的特色，平均一下，还是“中”麻。那后果就是直到现在，法律的精神还没有在中国建立起来，身边的人永远是首先可以违法的人。至于“平等”、“博爱”这些资产阶级的东西，在土地仍然是最主要的财富的中国，也仍然是被骂的对象。<br />
<br />
这就是我对论语的中心句的理解，基本没什么评论，但资料已经足够了。<br />
 <br />
附《新五代史&nbsp;&nbsp;卷二十&nbsp;&nbsp;周家人传第八》<br />
周太祖圣穆皇后柴氏，无子，养后兄守礼之子以为子，是为世宗。守礼字克让，以后族拜银青光禄大夫、检校吏部尚书、兼御史大夫。世宗即位，加金紫光禄大夫、检校司空、光禄卿。致仕，居于洛阳，终世宗之世，未尝至京师，而左右亦莫敢言，第以元舅礼之，而守礼亦颇恣横，尝杀人于市，有司有闻，世宗不问。是时，王溥、汪晏、王彦超、韩令坤等同时将相，皆有父在洛阳，与守礼朝夕往来，惟意所为，洛阳人多畏避之，号“十阿父。”守礼卒年七十二，官至太傅。北极星书库<br />
呜呼呜呼，父子之恩至矣！孟子言：舜为天子，而瞽叟杀人，则弃天下，窃负之而逃。以谓天下可无舜，不可无至公，舜可弃天下，不可刑其父，此为世立言之说也。然事固有不得如其意者多矣！盖天子有宗庙社稷之重、百官之卫、朝廷之严，其不幸有不得窃而逃，则如之何而可？予读周史，见守礼杀人，世宗寝而不问，盖进任天下重矣，而子于其父亦至矣，故宁受屈法之过，以申父子之道，其所以合于义者，盖知权也。君子之于事，择其轻重而处之耳。失刑轻，不孝重也。刑者所以禁人为非，孝者所以教人为善，其意一也，孰为重？刑一人，未必能使天下无杀人，而杀其父，灭天性而绝人道，孰为重？权其所谓轻重者，则天下虽不可弃，而父亦不可刑也。然则为舜与世宗者，宜如何无使瞽叟、守礼至于杀人，则可谓孝矣！然而有不得如其意，则择其轻重而处之焉。世宗之知权，明矣夫！</div>

							
							
							
							
