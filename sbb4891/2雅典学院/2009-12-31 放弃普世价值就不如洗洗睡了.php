&nbsp; &nbsp;&nbsp; &nbsp; 我认为自由派和其他政治主张以及当局相比最大的优势就是平和、理性。自由主义不主张暴力伤害任何人，而是旨在共和的基础上建立一个民主宪政的自由社会。自由主义和极端就是截然相反的词汇，根本不能共存。我们根本没有任何武力，更不用说和当局的千军万马飞机大炮相抗衡；自由主义的理论也没有什么煽动力，自由派从来不主张很有革命煽动的造反有理之类骇人听闻的话，更无意挑起社会阶层之间的敌视，我想你们也不好意思舔着脸在墙上刷个睡书记小老婆之类的标语。我们一无所有，只有普世价值的理性力量，但我相信这种优雅的力量是很厉害的，这是理性社会的状态，人们越是理性，自由就越强势。如果我们是为了自由而不是单纯的胜利，那我们应该做的是消除愚昧、传播理性；而暴力革命特别是一些嗜杀的主张，显然不能做到这点，只能让参与者变得仇恨和狂躁。民主成功的路径可以是街头运动，非暴力革命，甚至可以为了成功有节制的使用暴力，但是暴力绝对绝对不能成为目的，暴力和民主自由的矛盾使他们不能共为目的，而暴力的煽动性又让它很容易在革命中取代民主成为真正的目的。你要用清算共产党的仇恨战胜共产党，你就得变得比共产党更狠更加残暴不仁，假使你胜利了只能成为一个比当局更可怕的政权。这无非是机会主义者的游戏，我们的境况只能变得越来越糟。<br />
&nbsp; &nbsp;&nbsp; &nbsp; 即使外界环境再残酷，如果还算得上是自由派的话，我们唯一掌握的武器就是普世价值了，无论做什么事情，都要恪守人权的底限，自由主义的原则，否则还不如蛰伏。当然机会主义的暴力革命可以借用任何名头，天神，上帝，共产主义，也可以是民主自由。但我觉得一个扬言核弹炸北京、杀光中共官员的人观点和自由主义者相差太远了，而且自由主义者也不是什么造反的料，真的喜欢杀人的话不然搞个中国共产党（毛）之类的，团结工农，阶级斗争，农村包围城市.... 这样好像比较爽。<br />
