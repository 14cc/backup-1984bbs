													<h2>对民众和精英的不同要求</h2>
						
						
																											<div id="postmessage_615139" class="t_msgfont">　　对民众和精英，应该有不同的要求。最基本的，对于某种社会现象，民众可以不懂，可以有误解，这都是正常的；但精英不能说错话，说错了就不正常了，就SB了。<br />
　　就是这么简单的道理，很多人却给弄反了。他们对民众的误解嗤之以鼻，对精英的错误却非常宽容。要说明这种态度为什么错误，实在是有点多余，不过我要分析一下为什么会有这种错误。<br />
　　这源于这些人所受的教育。<br />
　　在教育，尤其是高等教育中，教师总是强调知识的重要性，鼓励学生：要敢于应用知识，不要害怕出错，出了错没关系，能从错误中积累知识是最重要的；没知识，就算没错，也是不行的。<br />
　　于是，他们就把这种思路套用到社会中来了，他们忘记了一个最基本的事实：社会是社会，学校是学校，正如厕所是厕所，厨房是厨房一样，不能乱用。<br />
　　他们对民众嗤之以鼻，是因为民众没有知识，但实际上，民众就不该成为相关问题的专家，就算是专家，他也不可能是所有问题的专家啊！对于任何一个特定的问题，大部分人都注定是不能精通的，这有什么奇怪？民众有错误，当然不好，想办法让他们认识到什么是正确的就好了，没必要鄙视民众无知，他们本来就不该有知。*<br />
　　另一方面，精英是有知识，但是这并不是他们犯错误的理由。老师可以原谅学生犯错误，是因为学生如果能在犯错误的过程中积累了知识，以后自然可以不犯。但是，精英当众犯了错误，这与其说能让他们积累知识，不如说会导致他们一直犯错误：他们要坚持自己的错误，才能保住面子。有这么一句话：“新的科学理论之所以能代替旧的科学理论，不是因为科学家们普遍接受了新理论，而是因为坚持旧理论的科学家都老死了。”就算没有利益之争，人们也是顽固的，更何况精英们的利益又往往大得很！<br />
　　所以，我们就不能原谅精英犯错误。就算我们不从牟利方面指责他们（指责也正常，许牟利还不许说么？），也要认定他们犯错误属于失职。<br />
<br />
　　顺便再讨论一下，什么叫精英的错误。<br />
　　有些人可能会认为，不同的学说本来就很多，某个精英的见解虽然是一家之言，但是你也不能因为所持的立场与他不同，就说他是错误的啊！<br />
　　这个话当然对。各种学说之间经常互相批驳，振振有词，就算你能振振有词地把某见解驳得一钱不值，也没什么奇怪；因为这不意味着你的那套学说就不曾被驳斥过。当然你要是没学说依据，只搞驳斥，就更没意思了；因为判断一种学说是否有价值，并不在于它有多坏，而在于它是不是比其它学说更好。<br />
　　但是，精英们说话，也不可能是照本宣科地把一种常见的学说复述一遍，那是没意义的，必会加点什么新意进去，至少得用来解释一些新鲜问题。这就要出错了。不要以为精英们的脑子非比寻常、滴水不漏，所谓利令智昏，这个时候，出什么低级错误的都有。这种错误一旦出来，我们就不要随便放过，理由如前所述。<br />
　　话说到这儿，有些人可能说了：这个有意思吗？<br />
　　当然有，还有两个方面的意思：<br />
　　一、精英犯错误，往往是故意造成恶劣影响，就算不是故意，也照样造成恶劣影响。不闹起来，把他们搞臭，就没办法抵消这些影响。<br />
　　二、即便不考虑社会影响，单从人品上说，这种事的实质，一般也不是“犯了这么个小错，值得被你这么侮辱吗？”；而是“犯了这么个小错，都不肯承认（一般如此），足见人品恶劣！”。<br />
<br />
<br />
*：如果你没办法让民众接受你的见解，那么毛病在你，而不在民众。卡尔 波普尔说过：每个知识分子都有一种特殊的责任。他拥有学习的特权和机会。作为对同胞或社会的回报，他有责任尽可能简单、清楚、谦虚地描述他的研究成果。知识分子所做的最糟糕的事情――主要的罪过――是试图对同胞自命为伟大的预言家，留给他们令人迷惑的哲学印象。任何不能简单、清楚地讲话的人都应该住口，继续下些功夫，直至能这样做为止。<br />
