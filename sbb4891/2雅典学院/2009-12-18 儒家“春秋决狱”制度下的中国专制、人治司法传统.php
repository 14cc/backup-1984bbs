													<h2>儒家“春秋决狱”制度下的中国专制、人治司法传统</h2>
						
						
																											<div id="postmessage_381055" class="t_msgfont">“春秋决狱”是西汉中期儒家代表人物董仲舒提出来的，是一种通过考察推测犯罪动机是否符合儒家《春秋》论著来做出判决的判案审理方式，主要用孔子的思想来对犯罪事实进行分析、定罪。即除了用法律外，可以用《易》、《诗》、《书》、《礼》、《乐》、《春秋》六经中的思想来作为判决案件的依据。《春秋》是孔子修订的一部鲁国的编年史。 通过皇权的力量要求司法官在遇到律无正文或虽有条文但不符合儒家道德的案子时，根据《春秋》经义断案，实际上赋予《春秋》经义极高的法律效力。 <br />
<br />
　　春秋决狱主要是根据案件的事实，追究犯罪人的动机来断案。如果他的动机是好的，那么一般要从轻处理，甚至可以免罪。如果动机是邪恶的，即使有好的结果，也要受到严厉的惩罚，犯罪未遂也要按照已遂处罚。首犯要从重处罚。 <br />
　　董仲舒有关的断狱案例还曾被汇编成十卷的《春秋决事比》，在两汉的司法实践中被经常引用。<br />
“春秋决狱”的推行和沿用，导致以人治超越法治成为中国司法正统，不按照“罪行法定”原则审理判案，而根据权力者的主管意愿审理案件，将中国专制司法推向顶峰。<br />
（1）春秋决狱过于强调行为者的主观性。强调“本其事”即强调根据客观事实判案，是正确的，但儒家学说在深入法律的过程中，将行为者的主观动机放在绝对起决定性的地位，只要其动机不符合儒家经典理论，就要加以重罚，而对于法律没有明确规定不定为犯罪的情况，则任意加以比附。这在司法实践中是极其有害的，因为，对于儒家经典中的“志善”、“志恶”的理解可以从多方面进行，也可以得出不同的结论，统治者完全可以根据自己的情趣和好恶定罪量刑，往往导致相同的犯罪其判决相去甚远。法律的公平、公正的目标难以实现。依据春秋所作的法律条文的解释过于主观化，破坏了法律的确定性，思想犯罪泛滥。 <br />
（2）春秋决狱加剧了司法的腐败。《春秋》一书的特点就在于文字银辉，可以有统治者根据自己的需要任意解释，在司法审判中，实际上没有客观严格的标准，在很大程度上是由各级官吏根据主观意志来判断的。而这些官吏在审案时常常以“微言圣意”作为借口，牵强附会，为满足自己的一己之私，故意曲解法律。在法律不健全的情况下，司法更为腐败。 <br />
（3）春秋决狱使得法律和政治、伦理之间界限更加模糊。在我国古代社会，法律和其它上层建筑和意识形态之间的界限本来就不是很清晰，汉统治者推行春秋决狱以后，儒家伦理道德从各个方面渗透到法律中去，而封建统治者为了维护专制主义中央集权和大一统，极力主张儒家思想在社会的各个层面发挥作用，甚至在某些司法活动中以儒家经典的伦理道德代替法律，最终按照统治者的意志实现对法律改造。 <br />
（4）春秋决狱加强了对人们的思想控制，同时达到了文化专制的目的。通过在法律上推行春秋决狱，并进行儒家思想其它各方面的渗透，将人们的思想禁锢在统治者规定的范围内，按照“君臣、父子、夫妇”之道规范人们的行为，通过教化和惩罚的双重手段渗透到人们日常的行为和思想中。法律的权威性和公正性在实践中受到严重的挑战，并使人们对于正义的追求也局限在儒家思想限定的范围内。经过几千年的流传和演化，统治者在加重对人们思想控制的同时，也达到了推行文化专制的目的。 <br />
<br />
&nbsp;&nbsp;春秋决狱以儒家思想作为法律上定罪量刑的依据，在融合了儒家、法家等诸多思想主张，确立了中国古代法律儒家化的标志的同时，也因为其判案的主观性强，司法官员经常以自己的个人好恶任意定罪，扩大了法律的随意性和任意性，背离了法律的公正性、正义性的要求。当代的法治建设在借鉴吸收传统法律思想精华的同时，必须坚持“以事实为依据，以法律为准绳”，在完备的法律制度的基础上，按照法律的要求规范法律技术和完善法律实践。<br />
<br />
原心定罪，以动机判刑 ，为专制大开方便之门<br />
<br />
由于《春秋》特别强调礼的作用和规范，强调“礼禁于未然之前”，所以，儒家学说的推行者们特别重视支配行为的动机是否符合礼的道德准则，认为“必本其事而原其志。志邪者不待成，首恶者罪特重，本直者论其轻。……罪同异论，其本殊也。”即在审理案件的时候，依据客观犯罪的事实，着重考察行为者的动机，只要有犯罪的行为动机，就可以加以惩罚，不必一定要等到犯罪真正出现时再施以惩处。对于首犯，应该从重处罚，只有犯罪行为而没有犯罪动机的人，应当从轻处罚。这种动机论曾被后人评论“《春秋》之治狱，论心定罪，志善而违与法者免，志恶而合于法者诛。”符合春秋中的“微言大义”就是“志善”，即使违法，也可以从轻处罚；相反，犯罪人主观动机严重违反儒家倡导的“忠”、“孝”之精神，即使没有造成严重危害后果的，也要认定为犯罪，并予以严惩， <br />
<br />
据记载，孔子处死少正卯就是“原心定罪”的典范，孔子任代理宰相之职，将学者少正卯处以死刑。罪状如下： <br />
第一、“心逆而险”。只是主观的认定他居心阴险，并无不法的行为。 <br />
第二、“行僻而坚”。行为怪诞，不接受劝告。 <br />
第三、“言伪而辩”。说谎话而坚持是事实。 <br />
第四、“记丑而博”。记忆力强，学问也渊博，但所知皆丑陋。 <br />
第五、“顺非而泽”。自己的错误，却把它粉饰为好事。 <br />
孔子强加给少正卯的五大罪名贯彻了他推行的“原心定罪”原则。以“心”作为定罪的根据。汉朝统治者推行《春秋》决狱，最基本的原则就是“原心定罪”。 <br />
&nbsp;&nbsp;春秋诀狱得到统治者认同的重要原因是它提倡“原心论罪”，即主张把行为人的主观方面主要是犯罪动机――“志善”或“志恶”作为罪行轻重或有罪无罪的标准和条件。既可以不顾客观事实，又可以摆脱法律形式的束缚，使执法者的任意决断达到登峰造极的地步。这种主观归罪自身所无法克服的缺陷带来许多司法上的弊端，最为显著的是必然导致同罪异罚的恶果，或是以“志善”为由为统治阶级的犯罪而开脱，或以“志恶”为由加重对广大民众的处罚，甚或一句《春秋》经义就可以使无辜的罪及九族，祸灭三族，正所谓，“志善而违于法者免，志恶而合于法者诛”。（《盐铁记・刑德篇》。）总之，春秋决狱本着君本主义的王权大一统政治哲学观，将亲亲相隐等儒家礼教礼治思想和原则作为立法、司法的基本原则，体现了统治阶级的利益和意志，达到了随心所欲驾驭民众的目的。<br />
&nbsp;&nbsp;另一方面，春秋决狱的实行，使得儒家的经典法律化，儒家的三纲五常的伦理道德与王权专制社会的刑法条文合二为一，形成约束人们行为的共同准则。不言而喻，这也必然为王权社会的罪刑擅断特别是君主之罪刑擅断大开方便之门，实力王权破坏法制创造了条件。罪刑擅断的滥觞，必然给不法之吏舞文弄法创造条件，造成司法的随意性，从而埋下“奸吏因缘为市，所欲活，则附生议，所欲陷，则予死比”（《汉书・刑法志》。）的司法腐败的根源。由此造成刑法的权威性越加受到干扰，致使罪刑法定原则无以体现和实施。<br />
综上所述，“春秋决狱”传统将中国古代专制司法推向顶峰，使得中国一直沉溺与以人治替代法治，靠政治权力垄断操控司法的集权专制体制中，至今贻害无穷。<br />
