													<h2>崔卫平：我们只有一个敌人    那就是专制――有关温家宝先生深圳讲话</h2>
						
						
																											<div id="postmessage_799494" class="t_msgfont">政治体制改革，是一笔体制内的遗产。正像每个人有他自己的梦想，政治体制改革便是这个体制曾经拥有的梦想（或理想）。如果每个人的梦想是允许存在的，体制的梦想也是允许存在的。只是，体制的梦想不等于每个人的梦想，体制的路径更不等于每个人的路径。<br />
 <br />
不能说既然个人有梦想，体制就不必有梦想，这种做法很像体制本身。这个体制曾经认为，不能拥有个人的梦想，因为体制本身就代表着梦想，就是梦想本身，因而将一切个人梦想视为仇敌。允许体制有它的梦想，是今天民主的做法。假如一个人能够更深地把握自己的梦想，他便能够更深地把握别人的梦想。<br />
 <br />
事实上，社会进步不可能是某一单方面梦想或理想的结果，而是各种梦想、诉求、利益互相之间平行四边形的合力。其中每一方力量都只是一个组成部分，而不是全部。学会听取各种声音，对于不同努力的人们抱有一种宽容的态度，在今天需要特别提出来。我们只有一个敌人，那就是专制。<br />
 <br />
无论如何（不管什么时候），政治体制改革这个梦想，是体制内部上升而不是堕落的要求，是体制内部民主而不是专制的要求，是体制想要面对社会、与社会进步相平行的要求，而不是固步自封、维护少数人利益的要求。至于它是否解决或者在多大程度上解决问题，那是另外一个问题，这实际上取决于我们所有人的努力。<br />
 <br />
悖论在于，这个来自体制内部的理想，被体制本身所葬送。这个体制拥有扼杀一切民主与社会进步的倾向，包括取消自身内部的进步力量，而试图继续堂而皇之地存在。二十一年前，作为国家领导人的赵紫阳先生，一夜之间在全国人民面前消失。他的名字，永远地与“政治体制改革”这个话题联系在一起。<br />
 <br />
赵紫阳先生的处境，成了这个体制的一个病灶；政治体制改革，是在这个体制内部还想做事情的人们的一块心病。那些凡是想要对这个民族的历史与现实承担责任的人们，那些想要将民族带往好的明天而不是坏的结局的人们，都不得不面对这个问题。这是绕不过去的，温家宝先生深知这一点。<br />
 <br />
温家宝先生的深圳讲话，在某种意义上回到了如今被看作赵紫阳先生遗嘱的这个东西。选择深圳这个地点不是没有意义。某种象征意义，完全可以与邓小平先生当年南巡讲话相匹配。我指的不是一个涉及经济，一个涉及政治，而是指同样都是在体制内部遇到强大的阻力之后，向社会发出的某种呼吁。<br />
 <br />
他们都想要在体制内部推进某种东西，在周围的人们身上推广某种东西，但是却发现推不动，阻力太大，困难程度难以想象。比较起来，温家宝先生更是如此。他为什么反复强调要“创新”、要“敢为天下先”，那是因为太多的人想当“缩头乌龟”。他们只想消耗体制的资源，享受体制的种种好处，却丝毫不想哪怕为这个体制尽一份力量，为体制本身负起责任，更不要说为社会、为公众尽力了。<br />
 <br />
社会应当听到这个呼吁。这个呼吁，与社会的利益相一致，而不是与特权的利益相一致；与民主的事业相一致，而不是与专制的力量相一致。所有那些为了民族的未来着想的声音，我们都应当听得见。运用一种“本质论”的眼光，来看待体制内所有的人们，是模仿对手的所作所为。<br />
 <br />
这次提出不让我去台湾的理由是，有些人我不能见到，见了就会怎么样怎么样。好像他们是恶意传染病菌，要采取某种隔离政策，我才能够健康。我们当中有些人也是这样，同样迷信隔离和隔绝，觉得那是最好的方案。这种隔离甚至更加彻底，从嘴巴上做起。不能提及某个名字，一旦提到就如何如何。<br />
 <br />
有人将温家宝与周恩来做类比。在我看来，周恩来不是民主派。温家宝先生是民主派。胡耀邦先生是民主派。赵紫阳先生是民主派。个人的力量总是有限的，就像我们个人的力量也是有限的。而当民主的力量互相打击时，这种力量就更为有限。<br />
 <br />
不妨这样想一想，实现中国民主这么一件大事，难道要将共产党完全排除在外？难道其中没有共产党的份额？当然这个问题，最好由共产党自己提出来。在不可阻挡的中国民主进程面前，它将扮演什么角色？将承担什么责任？是否要一劳永逸地将自己隔离出去？对于中国的未来，共产党人需要什么样的准备？<br />
 <br />
一个人获得自身起点之后，是为了与这个世界相平行，与他人相平行。<br />
