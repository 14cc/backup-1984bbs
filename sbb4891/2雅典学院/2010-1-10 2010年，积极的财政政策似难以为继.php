													<h2>2010年，积极的财政政策似难以为继</h2>
						
						
																											<div id="postmessage_424111" class="t_msgfont">2010年，积极的财政政策似难以为继<br />
刘松萝<br />
<br />
2009年8月4日到9月1日的一个月里，上海股市从3478点一直跌到2639点，下跌了839点。下跌的原因，主要是面对通胀预期，投资者担心积极的财政政策会发生改变。<br />
<br />
有意思的是，在沪市下跌期间，我们得到的信息是适度宽松的货币政策不变，不过要进行微调。但是，从具体事例来看，“微调”的幅度之大，足以和减速相比较。在深圳的突击抽查中，基金公司和证券公司的办公地点迎来了证监局工作人员的突然造访，部分笔记本电脑和台式机被封存并且带走。<br />
<br />
事态虽然出人意料，却也在情理之中。在此之前，就不断有传闻，声称有大量的贷款进入股市和房市。不论从管理层面还是道义层面来讲，这都是无法容忍的。由于房地产业一直在经济运行中拥有特殊地位，并且是特中之特，股市就提前受到了政策转变的影响。<br />
<br />
2009年9月29日，沪市再次探底2712点以后回升，一直反弹到11月24日的3361点后再次下调。<br />
<br />
由于对中央经济工作会议的预期，沪市再次回升。12月7日中央经济工作会议闭幕的消息发布以后，沪指再次回调。对此，经济评论家水皮先生在电视上提醒大家，经济工作会议提出了“要保持投资适度增长，重点用于完成在建项目，严格控制新上项目。”水皮指出，什么是“重点用于完成在建项目，严格控制新上项目”？这就是紧缩啊！<br />
<br />
2009年１２月２７日，温家宝总理接受了新华社的独家专访。他说，“过早地把我们刺激经济的政策退出的话，就可能前功尽弃，甚至使形势发生逆转。”不过他接下来又说，“我们已经开始关注到一些通胀预期的迹象，这对相当数量的中低收入者，尤其是贫困家庭，就会成为很大的负担。”关于房价，温家宝认为，“要维护房地产市场秩序，打击捂盘惜售、占地不用、哄抬房价等违法犯罪的行为。”<br />
<br />
近一段时间以来，政府发布了很多关于房地产的政策。这些政策的效果如何，尚需要观察。但是，把哄抬房价视为违法犯罪行为，是值得注意的。<br />
<br />
以上谈到上海股市，是想通过其走势说明机构和资金对于经济前景的反应。<br />
<br />
综上所述，2009年适度宽松的货币政策取得了积极的效果，同时也造成了不少的隐患。国内的消费需求并没有被激活，房价却出现了暴涨。房地产市场的疯狂，过高的房价收入比、售价租金比以及空置率等指标已经无法忽视。可以说，在抵御金融危机的过程中，我们正在制造更大的泡沫，孕育另一场金融危机。在2010年，积极的财政政策已经难以为继了。<br />
<br />
管理层的管理方式也值得我们注意。近半年以来，他们经常在保持既定的政策取向的同时，采取大幅度的调整措施。可以想见的是，假如不少投机者喜欢谈论通胀预期真的变成严重的通货膨胀，政策的突然转向不是不可能的。<br />
<br />
因此，我建议从事实业，从事投资和投机活动的人把运作风格从激进转变为稳健。假如情况发生变化，还应该转向保守。特别是从事房地产开发的企业，继续高价拿地和大规模圈地已经不合时宜。适度退却，保持足够的资金是必要的。参与炒房的人，一定要特别小心：如果长期持有10套以上的房产，在非常的情况下遭遇类似山西煤矿“重组”一类的情况，是有可能的。<br />
<br />
作为投机者，一定要知道政策不可能永远有利于自己，一定要知道政府不可能为自己承担风险。即使是投机的老手，部分地提前出局，预防突变也是必要的。<br />
<br />
2010.1.10.<br />
