													<h2>《网瘾战争》---境外敌对势力渗透大陆网游</h2>
						
						
																											<div id="postmessage_475498" class="t_msgfont"><strong>如此有娱乐效果的文章大家请慢慢欣赏</strong><br />
<br />
近日，一部名为《看你妹之网瘾战争》（以下简称为网瘾战争）的《魔兽世界》长篇视频以燎原之势迅速传遍了整个网络。与一般的网络游戏视频不同，这部长达64分钟且制作精良的所谓游戏电影并未将视角聚焦于游戏本身，而是集中渲染了2009年度中国大陆的各类负面新闻，并试图将矛头指向政府机构。通过分析，可以发现该片的制作很可能经由境外势力授意。<br />
<br />
<br />
<br />
一、&nbsp;&nbsp;网瘾战争，谁与谁的战斗？<br />
视频《网瘾战争》的标题冠以战争之名，其用意就在于凸显对立的双方。从表面上看，视频中“战争”的参与者分别是《魔兽世界》的普通游戏玩家与以开办网瘾中心闻名的杨永信。然而从深层次来看，争战双方的身份实则另有深意。<br />
<br />
<br />
《网瘾战争》中杨永信一方则被刻画为鱼肉百姓、独裁暴政的形象，片中别有意味地强调杨永信曾领取过国务院政府特殊津贴，而在影片结局杨永信被《魔兽世界》玩家击败后为又刻意安排台词道：“我背后的力量，轻而易举就能把你们所有玩家的声音淹没。”这种前后关联手法的唯一目的就是引导普通观众将所谓杨永信“背后的力量”与发布特殊津贴的国家政府机关等同起来。<br />
<br />
<br />
<br />
同时在片中被列为反面角色的还有我国的文化局和新闻出版总署。片中分别将其丑化为“萌化大神”和“版书大神”这两个类似于黑社会大佬的形象。情节上则直接讽刺文化局和新闻出版总署平日里勾心斗角，面对群众时又狼狈为奸。<br />
<br />
<br />
<br />
反观片中《魔兽世界》的游戏玩家，他们被打上了“自由”、“民权”、“弱势群体”等标签。作为影片中正面角色的《魔兽世界》游戏玩家群体“骷髅党”被描写为为中国网络反抗暴政和不公的自由民权斗士代表。作者在接受凤凰卫视采访时表示：“这个影片，让不懂游戏的人流泪，是因为大家的互联网处境都一样。”他试图将认同者的范围从《魔兽世界》的游戏玩家扩张至整个互联网上的所有网民。<br />
<br />
<br />
<br />
以上证据都指向了同一个事实，即视频作者试图将《网瘾战争》刻画为全体网民、自由民权斗士与中国政府之间的战争。<br />
<br />
<br />
<br />
二、理性批判还是恶意煽动？<br />
<br />
<br />
《网瘾战争》这部《魔兽世界》的相关视频却几乎没有涉及到游戏本身的故事背景和世界观，反之加入了大量对社会现象的批判和讽刺。那么这究竟是善意的还是恶意的？是理性批判还是恶意煽动呢？<br />
<br />
<br />
此片参杂了70码、钓鱼执法、南京的彭宇案、临时性即意犯罪、华南虎照片事件等社会内容，以上均涉及法院、警署、林业厅等政府机构，显然经过精心编排，矛头直接指向政府国家机关。然而视频中并没有任何一处表现出对于这些事件的理性思考或是诚挚建议，可见编排此类事件并非出于社会责任感，而仅仅是通过公共事件引起观看者的共鸣。其效果就是渲染对政府的不信任情绪。<br />
<br />
<br />
事实上，影片中存在多处此类不负责任的所谓批判。最为令人失望的是，这些质疑并没有转化为对社会问题的思考，其作用全然在于为影片高潮部分煽风点火。<br />
<br />
<br />
《网瘾战争》的高潮处由一段主人公独白和煽动观众举手这两部分组成：<br />
<br />
<br />
在独白部分，影片采用了和纳粹及X轮功相似的宣传手法。通过情感充沛、歇斯底里式的呐喊，再一次泣诉了在国内生活现状的凄惨和对美好生活的向往，与之前描写社会事件的铺垫相互呼应。该段落纯粹以抒情说服观众，麻痹观众思考。正常情况下，任何一款游戏在任何一个国家上市，既需要符合发行公司的商业利益，更需要符合该国的相关法律。而独白中则仅仅因为国内《魔兽世界》的版本与海外不同，便斥之为扭曲，上纲上线为落后于世界上的其他民族，不顾世界上大量国家甚至日本等发达国家均从未引进过《魔兽世界》的事实，将简单的电子游戏问题夸张为政治民生问题。同时独白竭力渲染除国内不及国外、千辛万苦也要去欧美台湾的情绪。<br />
<br />
<br />
而煽动观众举手的部分，则是由一段无声的字幕引出。这段字幕的文字部分改编自犹太人屠杀纪念碑，自比被纳粹德国屠戮的犹太人，进一步加强观众的怜悯之情。这一无声片段强化了压抑感，为观众之后举手作为宣泄的时刻留下了空间。高潮部分的最后一刻片中主角以集结众力的形式，号召所有观众举手赞同《网瘾战争》的意识形态，引导观众在狂热的情绪下听命于视频。普通观众很容易误解这种被控制的行为完全出于自愿，最后应和着“我们是魔兽玩家！”的煽动性词句完成了身份认同的全面转化，彻底被剥夺了思考的权利。其整个高潮部分的煽动方式相当成熟老练。<br />
<br />
<br />
视频发布后，还有为数不少的网民表示几乎每天都会看一遍这段超过1小时的视频，并深深为其感动和流泪。其煽动性接近宗教信仰，很多观众在观看高潮部分都会对其中不合理的语句熟视无睹，少部分认识到其煽动性的观众则遭受了来自网络的恶语相向。<br />
<br />
<br />
<br />
由此可见，《网瘾战争》这一视频拥有极强的煽动性，据悉有大量观众按照视频所言举起手臂，以示赞同。如果有后继视频煽动观众游行示威及进行其他更为过激的活动，后果将不堪设想。<br />
<br />
<br />
<br />
三、谁才是真正的幕后推动者？<br />
<br />
<br />
<br />
视频作者在与记者闾丘露薇对谈中透露，这一视频耗时3个月，超过100人以上参与了视频的制作。该数字远远超过了常见游戏视频的制作时限和人数上限，几乎与商业用途的宣传片、纪录片无异。很难想象一个自发的玩家团体在没有任何组织机构的支持下，可以自发地完成这样一部效果出众的游戏视频。<br />
除此之外，《网瘾战争》的制作立场存在一个疑点。与片中宣扬自己代表劳苦大众的姿态不同，影片制作者似乎对中国的现状不甚明了。在其煽动部分的高潮处出现了“打开那扇月租2000块的房门”等词句，须知即便是在京沪等大城市，2000元/月的月租房对于一般白领而言也是相当高昂的价格，而这一价位所对应的住宅一般都是较为高档的公寓，与后文对应的“住着连体排屋”断然对不上号。这显示出视频作者对中国大陆普通老百姓的生活状况并不如其所伪装得如此清楚，而是以更发达地区居民的视角地肆意编造事实。<br />
<br />
另一个值得注意的事实是该视频一经推出，台湾的《工商时报》、《中国时报》争相对其进行了详细报道，在报道中均对该视频的主要观点做出了肯定和赞扬。同时，如果仔细查看该片的制作名单，可以发现大量参与人员均来自于《魔兽世界》的台湾服务器。而影片的图像素材也大多录制于《魔兽世界》的台湾版本。这些信息无疑都暗示着该片的制作背景。<br />
<br />
<br />
<br />
《网瘾战争》的高潮处特别提到即使被称之为“大陆蝗虫”，也要克服极大的阻碍下在“台服”（即《魔兽世界》台湾服务器）进行游戏。作者接受采访时表明“大部分热爱这个游戏的人，都早已离开了自己国家的服务器……但我们依然不愿意在自己的国家的服务器来玩”，还称“这并不是玩家的问题。”<br />
<br />
<br />
上述证据均显示《网瘾战争》很可能是在境外势力的扶持下制作的，而由影片的实际内容进行判断，这一势力最大的可能莫过于台独分裂分子。<br />
<br />
<br />
<br />
四、境外敌对势力新手段<br />
<br />
<br />
如果这一次《网瘾战争》确实是由台独分裂分子利用少数大陆魔兽玩家制作的糖衣炮弹，那有必要引起有关部门高度重视，因为该片的诞生蕴含着网络台独分子分裂手段的两大新变化。<br />
<br />
<br />
第一个新变化是宣传途径的变更。台独分裂势力首次通过大陆网络游戏进行台独思想的宣传。他们通过网络游戏这一广大人民喜闻乐见的娱乐平台散播其思想价值，计划实现分裂祖国的阴谋。而《网瘾战争》则是这一新渠道的试行。事实表明，此次《网瘾战争》达到了病毒式的传播速度。<br />
<br />
<br />
第二个新变化是宣传重点的改变。原本台独分子的主要意图在于分裂台湾与中国大陆，其宣传着眼点也着重与此。幸运的是，长期以来我国政府在反国家分裂方面作出了不懈努力，民众对于对于台独的危害有着清醒的认识，均深刻认同台湾是我国不可分裂的一部分。于是台独分子曲线救国，试图首先宣传分裂主义，继而宣传台独。<br />
<br />
<br />
此次《网瘾战争》正是这一思路下的新产物。互联网在早期常被认为是一块独立的国度，不少网民并不适应网络由政府监管和保护。因此台独分子尝试分裂互联网社会与现实社会，以此实现宣扬分裂主义的野心。<br />
