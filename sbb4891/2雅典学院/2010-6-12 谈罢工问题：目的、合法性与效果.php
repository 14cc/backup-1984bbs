<br />
对第二项批评我的看法是：<br />
<br />
失业率与劳动工资水平有关，但只是弱相关，影响失业率的最重要和最直接因素是经济周期。经济增长失业率自然降低，经济萎缩即使保持低工资政策也会出现大量失业现象，比如中国的文革时期。提高就业率的有效方法不是低工资政策，而是加大教育投资、提高就业能力、营造宽松的投资环境和创业环境，促进创新和新兴产业的兴起等。如果以低工资作为降低失业率的手段，思路上无异于一块小蛋糕平均分配的社会主义思想，极端状态就是奴隶劳动。<br />
<br />
