																											<div id="postmessage_681728" class="t_msgfont">下面谈最后两点：为什么要工会和罢工的社会影响。<br />
<br />
童话里有一个给猫拴铃铛的故事，大意是老鼠们意识到，假如可以在猫脖子上拴个铃铛，那么它们的小命就会大有保障。问题是谁会愿意冒着赔掉小命给猫拴铃铛？<br />
<br />
这样的问题也是人类面对的问题，独裁者或占据支配地位的政党怎样才能通过规模较小的军队长期控制数目庞大的人群，众多乘客面对一两个劫机者就会无计可施，束手就擒？上面的例子中只要大多数人同时行动就很容易成功。不过统一行动少不了沟通与合作，偏偏沟通与合作在这个时候变得非常困难，而压迫者由于深知群众力量有多大，会采取特殊措施阻挠他们的沟通与合作。一旦人们不得不单独行动，希望聚沙成塔，集腋成裘，问题就出来了：“谁该第一个采取行动”？<br />
<br />
在苏共二十大上，赫鲁晓夫首次谴责斯大林的大清洗运动，他的讲话刚结束听众中就有人大叫，质问赫鲁晓夫自己那时候在做什么。赫鲁晓夫的反应是清提问者站出来，报出自己的姓名，听众一片沉默。赫鲁晓夫答道：“当时我也是这么做的。”<br />
<br />
以上这段引自迪克西特和奈而伯夫著的“策略思维”。既然是策略，就是双刃剑，你可破解，我就要反破解。由于一方掌握资源有决策权，另一方不掌握资源没有决策权，管理者与个体雇员之间的谈判为前者提供了很不合理的优势，这就有如上面的例子，为了抑制这种优势，雇员必须联合起来谈判，而为了破解谁给猫解铃，避免沟通和联合行动的发起者付出重大代价，集体谈判，包括工会和单独维权处于劣势人群的组织面对具有不合理优势者集体谈判在许多国家成为一种制度。<br />
<br />
<br />
<br />
在社会影响方面，对工会和罢工的批评主要来自三个方面：<br />
<br />
1、&nbsp; &nbsp; &nbsp; &nbsp; 工会要价过高，这种诉求会摧毁一个行业；<br />
2、&nbsp; &nbsp; &nbsp; &nbsp; 工资的提高使受雇人员受益，但使雇主为了削减劳动成本而不愿多雇人，从而增加失业人口；<br />
3、&nbsp; &nbsp; &nbsp; &nbsp; 罢工对社会经济造成伤害。<br />
<br />
对于第一项批评我的看法是：<br />
<br />
1、&nbsp; &nbsp; &nbsp; &nbsp; 经济发展不是最终目的，社会幸福指数的提升才是目的；<br />
<br />
