																											<div id="postmessage_681590" class="t_msgfont">昨日忙碌，字未写半页，书未读一篇。此文已被耽搁，今趁休息续完。<br />
<br />
经济目的的有效性从两个方面来谈：一个是普遍性诉求，另一个是对突发事件的诉求。<br />
<br />
普遍性诉求是指绝大多数雇员的要求，比如提高生活水平的要求和通胀造成生活水平下降引起的加薪要求等。个别企业工资水平低于行业平均工资水平的情况一般无需发展到罢工这一步，甚至无需集体谈判即可解决。因为同行业的工资差异会造成人员流动，工资水平较低的企业因跳槽现象导致人才流失。<br />
<br />
当一个行业工资水平低于社会平均水平，或一个行业工资增长较长时间跟不上行业发展时，经济诉求就会成为普遍诉求，由于同行业企业在同一工资水平上就造成两种现象：<br />
<br />
1、&nbsp; &nbsp; &nbsp; &nbsp; 由于工资决定生产成本，涨工资就意味着在行业内处于竞争劣势，因此每个企业都不愿率先涨工资；<br />
2、&nbsp; &nbsp; &nbsp; &nbsp; 由于同行业处于同一工资水平，职业技能原因使跨行业跳槽者失去职业优势，不容易获得较高工资，行业性低工资在短期内不会造成人才流失。<br />
<br />
鉴于以上两点，每个企业都失去涨工资的动力，集体谈判涨工资就成了雇员提高收入的最佳途径。罢工作为经济施压手段可能导致整个行业瘫痪，因此成为一种有效的谈判工具。<br />
<br />
在不同的经济周期各个行业的状况会完全不同，国家政策和国际环境也会对各个行业造成不同影响。因此经济诉求为目的的罢工一般表现为行业性，比如煤矿工人大罢工、纺织工人大罢工和汽车制造工人大罢工等。全国性总罢工则通常表现为政治性罢工。<br />
<br />
对突发事件处理不当引起的罢工具有政治性，由于是为改善工作环境做的努力，因而也可归为经济诉求。<br />
<br />
突发事件引发的罢工局限于一个企业内，也可能引起其它企业雇员的声援性罢工而加重政治色彩。罢工这种边缘策略导致的企业瘫痪会使问题迅速得到解决。<br />
<br />
这里我们必须注意两点：<br />
<br />
1、&nbsp; &nbsp; &nbsp; &nbsp; 集体谈判某种程度上类似于婚姻契约，劳资双方承认在利益上继续存在差异的可能性，但希望用不同的原理来加以调整和解决；<br />
2、&nbsp; &nbsp; &nbsp; &nbsp; 政府作为中间人的调解作用或压制作用，以及作为经济政策制定者对双方利益调整的作用。<br />
<br />
<br />
对于咖啡馆兄提及罢工的长期有效性和短期有效性问题，我这正是我想讨论的一个内容。<br />
<br />
集体谈判，包括罢工的经济动机是把各方经济利益通过人为干预迅速拉向均衡点，因此必然是短期性的。长期来说，只有市场力量起作用，即使没有集体谈判市场也会把工资水平拉到均衡点。但生活是现实的，谁能等到古典经济学鼓吹的市场的长期效应？凯恩斯对此调侃说，从长期来说，我们都会死去。<br />
