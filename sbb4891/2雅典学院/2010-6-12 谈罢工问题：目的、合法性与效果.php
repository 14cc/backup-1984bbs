													<h2>谈罢工问题：目的、合法性与效果</h2>
						
						
																											<div id="postmessage_676777" class="t_msgfont">由富士康N连跳的跳楼事件和深圳罢工事件引发出工会及罢工的合法性问题。我认为，探讨工会及罢工的合法性问题应该从以下四点入手：<br />
<br />
1、&nbsp; &nbsp; &nbsp; &nbsp; 罢工的目的；<br />
2、&nbsp; &nbsp; &nbsp; &nbsp; 罢工的合法性；<br />
3、&nbsp; &nbsp; &nbsp; &nbsp; 罢工对达成目的是否有效；<br />
4、&nbsp; &nbsp; &nbsp; &nbsp; 为什么是工会；<br />
5、&nbsp; &nbsp; &nbsp; &nbsp; 罢工对就业人群、失业人群，以及社会的影响。<br />
<br />
这里的讨论可能需要少许经济学知识的准备，不过经济学本来也来源于生活，没有经济学知识准备，有生活阅历的人如果仔细思考也不妨碍理解和参与。<br />
<br />
一、&nbsp; &nbsp; &nbsp; &nbsp; 罢工的目的<br />
<br />
我观察争论各方，包括铅笔社都把注意力仅集中于罢工的经济动机，其实罢工动机可分为两种：经济动机和政治动机。<br />
<br />
经济动机包括提高工资、改善工作条件和缩短劳动时间。相对于休息而言，劳动具有痛苦性，痛苦是一种成本，改善工作环境可减少痛苦成本；休息也是一种报酬，同等工资水平下劳动时间的缩短等价于提高工资。因此，经济动机可简化为提高工资这单一动机的讨论。<br />
<br />
其实，许多时候罢工是出于政治目的。遗憾的是，从近期的讨论来看，人们把罢工的政治动机忽视了。<br />
<br />
与示威游行一样，罢工经常被用于政治表达。尤其像全国性罢工这种超越企业和行业的罢工很难提出具体的经济诉求，这样的罢工更是一种表达，向社会的强势政治组织或政治人物施压，以迫使其放弃某一政治主张或接受某一政治主张。<br />
<br />
二、&nbsp; &nbsp; &nbsp; &nbsp; 罢工的合法性<br />
<br />
弄清了罢工目的之后，我们可以来讨论罢工的合法性问题了。为了简单起见，我们先讨论出于政治目的罢工的合法性。<br />
<br />
既然上面我们提到罢工与游行示威一样是一种政治表达方式，也就与游行示威一样，罢工成为人们的一项政治权利。这个政治权利成为基本人权写入绝大多数国家的宪法，成为基本人权之一。1975宪法审议时针对未写入罢工权利，毛泽东亲笔将这个权利添上，并说这是工人的基本权利不能剥夺。<br />
<br />
改革开放之初由于国家经济濒临破产，为了成功地把工作重点向经济建设转移，也为了消除十年动乱后遗症，罢工权利从宪法中消失了。当时中国各个方面向好，政治开明经济起步，人们对政治体制改革和经济体制改革都满怀希望，因此罢工权利未写入宪法并没引起太多非议。不过尽管宪法保障的公民基本权利未列入罢工权利，但也没有任何一部法律禁止罢工，因此罢工并不违法，只是不能得到法律保护。具体地说，如果宪法保障罢工权利，雇主因为罢工解雇你的话，你可以将他告上法庭，你将胜诉。在罢工得不到法律保障的条件下，雇主可以因为你罢工而解雇你。但是，如果你的政治诉求合理的话，雇主因罢工解雇你会遭受相应的政治损失。<br />
<br />
<br />
网文长了没人看，这节写到这里，下节写罢工的经济动机合法性和对目标达成的有效性。</div>

							
							
							
							
