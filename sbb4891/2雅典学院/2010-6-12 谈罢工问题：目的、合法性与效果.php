																											<div id="postmessage_677015" class="t_msgfont">（续前）<br />
<br />
许多初通经济学和自由主义的人（比如铅笔社诸君）喜欢用工资由供求关系决定来反对雇员与资方谈判决定工资。谈清楚这个问题就不能回避两个基本规律：一个是工资对物价变化的滞后性；另一个就是工资和岗位的粘性。<br />
<br />
先说第一个，其实很简单，对于物价上涨因素产生的涨工资要求来说,必先有物价变化，才有人们对工资的要求。物价上涨首先反映于部分商品的价格上涨，这些商品价格又拉动其它商品价格，最后才能体现出劳动力价格上涨。因此，劳动力市场供求关系决定的价格一定滞后于其它商品的价格。通货紧缩期间厂方倾向于压缩生产和裁员，经济上升期间雇主才倾向于雇佣新员工，而经济上升期间往往伴随着物价上涨。因此，新雇员工的工资水平一定低于均衡条件下的市场劳动力价格。有工作经历的人都知道，工资只能每年调一次，或几年集中调一次，不可能随行就市地调整，也就是说工资具有“粘性”，雇主从劳动力成本考虑也希望工资保持在低于市场均衡价格。因此存在受雇后的工资二次谈判要求，罢工作为向雇主施压，同时承担被解雇风险的一种激烈的谈判形式是经济目的的合法性来源之一。<br />
 <br />
有人会说，既然你的老板给你的工资低于劳动力均衡价格，为什么不寻找工资较高的工作，而是采用罢工这个极端的方式来解决？这是因为岗位的粘性。一个人失去现在的岗位，即使能找到供求关系均衡劳动力价格的工资，也可能面临一段失业期，这就是经济学上所谓的摩擦性失业。较低工资水平上要求较高工资面临的生活风险远小于面临一段失业期的风险。<br />
<br />
还有一种情况，经过一段时间的工作或工作培训，工人的熟练程度显著提高，职业技能充分发挥，其劳动力市场价值变得高于新雇时期的工资水平，对于工资的重新谈判也在所难免。<br />
<br />
从以上讨论来看，认为工人通过罢工要求经济权益的满足违反合同的观点显然站不住脚，如果你承认工资不完全由劳动力市场的供求关系决定，承认二次谈判具有合法性，那么承认罢工作为谈判的激烈形式的合法性也就成了逻辑必然。（注：一般来说，罢工期间不支付工资，这也是谈不上违约的原因之一）<br />
<br />
关于场外竞争，跳槽等，将在罢工的有效性一节讨论。<br />
