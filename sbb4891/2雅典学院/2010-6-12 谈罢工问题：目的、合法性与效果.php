接下来我们讨论罢工的有效性问题，所谓罢工的有效性就是是否能达成罢工所诉求的目的。<br />
<br />
我们还是从罢工的政治目的开始讨论。诉诸罢工作为政治工具一般出现于下述两种情况下：<br />
<br />
1、非民主国家无法通过选票达成政治诉求，罢工作为游行示威和街垒的中间形式，同时也是比街垒更和平也更有效的方式。因为军事对抗的结果往往有利于有强力者，而不利于有道理者；<br />
<br />
2、民主社会中，当届政府的政策法令遭到普遍反对，又无意改变时，两届大选之间不可能用选票阻止一项政策的实施，游行示威等街头抗议在所难免，罢工是其中最激烈的表达形式之一。<br />
<br />
如前所述，政治性罢工由于诉求的广泛性，必然是全国性的，目的是通过罢工造成整个社会的经济瘫痪来给施政者施压。由于经济瘫痪，以致经济破产将引发社会动荡和政局失稳，最终导致政府垮台。因此，一般来说，全国性罢工提出的政治诉求都能实现。<br />
<br />
下一节我们谈经济目的罢工的有效性和为什么组织工会。<br />
<br />
（小常识：个人发表诉求声明后不上班不能称作罢工，只能算作旷工和自动离职。罢工定义为集体谈判中不能达成一致时动用的经济压力之一种，与罢工这一经济压力对应的雇方动用的压力形式可能是不准工人入厂）</div>

							
							
							
							
