																											<div id="postmessage_684752" class="t_msgfont">（注：一般来说，罢工期间不支付工资，这也是谈不上违约的原因之一）<br />
==============================================================<br />
这一点不是的。<br />
罢工期间不支付工资，仍然是工人违约，或者说属于工人旷工，违反了纪律。<br />
合同约定，在某某期限内，工人得给资方工作，如果不想工作，可以提出辞职，从辞职获准到实际离职，得有一个缓冲期间给资方寻找新的替代工人的机会。<br />
突然离职不干，会给资方造成损失，属于违约或者属于旷工，工人应当接受惩罚。<br />
<br />
<br />
另外，民法有个原则，叫“情势变更原则”，意思是刚开始定的合约，约定了价格，但是由于现实情况发生了重大变化，导致继续履行合同的话，会显失公平（即明显的不公平，不是很明显的不公平，则不算），那么可以变更合同或者废止合同。<br />
这个原则特别强调的是，现实情况发生重大变化，并不包含普通的商业风险。普通的商业风险是指可以预料的正常的价格波动，通货膨胀显然是可以预料的，一点点通货膨胀也是属于正常现象。<br />
像数十年难遇的金融危机、百年难遇的地震，这些才适用情势变更原则。<br />
所以，lz认为通货膨胀、物价上涨，就可以成为罢工的理论依据，是有那么一点点值得商榷的地方。<br />
<br />
合约固然要强调公平公正，但是外在的现实因素的变动导致合约的公正性受损了，如果不是特别严重的话，还是保持合约的稳定性比较好，稳定性意即可预见性。太强调公平公正有的时候就会失去可预见性。<br />
