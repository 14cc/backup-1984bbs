													<h2>任志强：北京不必抑制房价 民工都买得起</h2>
						
						
																											<div id="postmessage_449024" class="t_msgfont">新华网1月24日报道&nbsp;&nbsp;俗话说“道不同，不相为谋”，但这次两位“道不同”的委员不但同上两会，还愉快地成了“室友”。他们就是京城房地产界的大腕儿――万科副总裁毛大庆与华远董事长任志强。毛大庆坚持认为随着大批保障性住房入市，2010年的北京楼市将持续平稳运行；任志强则连连摇头，认为保障房入市跟商品房价格涨跌没关系，商品房价格会接着往上涨。<br />
<br />
保障房入市保证房价平稳<br />
<br />
昨天上午10点，市政协委员、万科企业股份有限公司副总裁毛大庆刚一露脸，众多记者就问他如何看待2010年的房价。毛大庆表示：“今年房价会比去年平稳，不会出现大起大落的情况，特别是今年上半年还承载着去年房地产价格的惯性，态势会维持一段时间，但到了下半年，随着很多保障房入市，情况会得到缓解。”<br />
<br />
与此相比，市政协委员、北京市华远集团有限公司董事长任志强的出现有些悄无声息，好多媒体都没注意到他。记者一路跟随任志强来到房间，问了句“您和谁住一屋啊？”任志强想都没想就答道：“我跟毛大庆一屋儿。”房间的写字台上，毛大庆已给任志强留了张便笺：“任总：我有事出去一会儿。”<br />
<br />
不加大供地房价还会上涨<br />
作为房地产大腕，任志强表示，北京不必抑制房价，它会接着往上涨，“电视上都播了，天津、广东的农民工通过努力在北京买了房、结了婚，连农民工都买得起房，你为什么买不起呢？”任志强认为，买房不一定非要在三环以内，如果觉得房价高，可以买距城区远一点儿的房子。“老说我们不给中低收入家庭盖房子，但中低收入家庭是否买得起房是政府的事，和房地产商没关系。”<br />
<br />
大量保障房入市是否会拉低北京高房价？任志强表示：“没什么影响，商品房和保障房是两回事，有钱人买商品房，中低收入家庭买保障房，这不是很清楚的事吗？”任志强认为，如果不加大房地产的土地供应，那么商品房价格还将继续上涨。（北京晨报）</div>

							
							
							
							
