																											<div id="postmessage_130984" class="t_msgfont">作者: 新浪博文<br />
<br />
全美警界最著名的华裔李昌钰先生，近日就大陆沸腾一时的邓玉娇案接受记者李三思专访。李昌钰先生回忆说，去年周正龙事件时，他注意到很多大陆网友邀请他出面鉴定，他本人正是由此开始关注大陆网站。李昌钰先生说，邓玉娇案他也注意到了，并一直十分关注。<br />
<br />
<br />
当记者问邓玉娇案假如发生在美国是否有罪时，李昌钰先生说：“有罪？那还有什么叫无罪？”李昌钰先生说，假如邓玉娇案真的发生在美国，根本不可能受到审判，正好相反，受到审判的将是邓贵大等人，而邓玉娇作为受害者，将提出巨额赔偿。 ...<br />
<br />
<br />
记者说邓玉娇案一审结束后大陆很多刑法学家们纷纷表态，对案件的结果表示支持，如中国著名法学家马克昌、武汉大学刑事法律专业博士生导师康均心、中国法学会刑事诉讼法学会理事高一飞……，李昌钰先生打断记者的话：“他们不配称刑法学家！”李昌钰先生连声反问：“案件发生后他们干什么去了？”“为什么要到案件审理结束了再出来说话？”李昌钰先生说，作为一名刑法学家，他认为自己的主要责任是还原证据，通过证据去确立法律依据，而不是对案件结果说三道四，那应该留给公众去评 ...</div>

							
							
							
							
