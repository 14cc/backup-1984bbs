													<h2>秦晖：法兰克福研讨会风波</h2>
						
						
																											<div id="postmessage_251950" class="t_msgfont">会前风云<br />
<br />
　　法兰克福书展是国际上最有影响的图书盛会之一，今年中国是书展的“主宾国”，作为书展的“前奏”活动，9月12-13日主办方在法兰克福的塞万提斯西班牙文化中心举行了“中国与世界――感受与现实”国际研讨会。我是德国主办方邀请的发言嘉宾之一。几个月前他们就与我联系此事，并且要了我的家庭地址说是要给我用快递寄发正式邀请书。然而我却一直没有收到。直到约半个月前，才从我任教的清华大学国际处转来了这份邀请书，学校说是国家新闻出版署转来的，我在校国际处看到新闻出版署发来的这个大信封中有好几份邀请，但只有人文学院李强院长的一份附有保险、酒店订单等签证资料，其余几份都没有，觉得很奇怪。打电话问新闻出版署，回答说本次嘉宾是中德两方各自邀请的，李强院长属中方邀请，由新闻出版署组团前往，一应手续也由公家办理；我们则是德方邀请，费用由德方出，手续需自办。但既然是德方邀请，为什么邀请书却不直接寄给我，而要由新闻出版署代转？我当时想，大概是德方为了表示尊重中方意见，所以把他们发的邀请书都请中国官方“把关”吧。<br />
　　由于8月底起我要到东南亚参加预期半个月的湄公河流域开发调研项目，回来后估计是赶不上趟了，因此我一直没向德国方面肯定行期。等到我9月10日从老挝回来才知道居然还来得及，于是次日便乘汉莎航班飞往德国。此前我由于一直在东南亚，并不知道这期间发生了什么纠纷。但登机前却得知德方邀请的其他几位大多没有来，不免有些奇怪。<br />
　　直到在飞机上不期而遇到戴晴女士（她的女儿十多年前曾经是我的研究生，我们自然早就认识），这才知道原来为这研讨会已经发生了一场大冲突。戴晴女士说：德方将她的邀请书寄到中国新闻出版署后，新闻出版署表示抗议并退回了邀请，德国书展主办者顺从中国官方意愿，取消了对她的邀请。但德国舆论闻讯大哗，认为在德国搞书展请什么人还要看中国官方的脸色，这还了得？舆情沸腾中，书展参办方德国笔会坚持再次向戴晴发出邀请。戴晴表示本来这种活动她未必感兴趣，但官方既然这么阻挠，为了捍卫言论自由和申明自己的权利，她倒是非去不可了。不料她赶到机场时却被告知：原订的机票已被取消了――显然是书展主办者在某种压力下cancel了这张机票。倔强的戴晴便索性自己买了张机票，终于成行。<br />
　　到了法兰克福机场，我因为坐在前舱又无托运行李，就先出了机场，见到前来接机的书展主办者彼得. 里布肯先生，他手里的接机牌上写着我和徐星的名字，没有戴晴，表明他来前还不知道戴晴已经成行，但此时他当然已经知道，因为大批记者已经在此守候。这时有人塞给我一张中国官方媒体《环球时报》，这份当天（11日）的报纸已经报道了此事，标题便是《研讨会突邀不速之客，中方组织者严词拒绝，德媒借书展恶毒攻击中国》，内称：“德方未经中方同意，邀请异见人士参加研讨会。中国方面已经表明立场，如果德方不放弃邀请，中方将不出席此次研讨会。”该文还引述德国《明镜》周刊采访里布肯时后者的话：中国方面“十分坚决地要求，‘如果这两人出席，我们就离开’。”该文又引述“中国社科院学者赵俊杰”的话说：“这次事件是中欧围绕价值观的一次争论和较量。我认为，对于公理和正义之举就要坚持，丝毫不能妥协，爱国主义不能打折扣。”看来，赵先生所谓的“爱国主义”居然是针对戴晴等中国公民的，俨然是有她无我，有我无她，在封杀这些中国人的问题上“不能妥协”，否则赵先生的“爱国主义”就打了“折扣”？！<br />
　　从这张《环球时报》上我还第一次得知除戴晴外此事还涉及旅居美国的 “异见”诗人贝岭。贝岭人在美国，他的邀请书应该不是寄到新闻出版署的，但是“中方”不同意，主办者也就收回了邀请。后来德国笔会坚邀戴晴，似乎没有提到贝岭，但贝岭大概并不知道邀请被取消，仍然来到了法兰克福。而此时德国已是舆论汹汹，似乎如果拒绝了两人，书展就要变成“丑闻”了。<br />
　　从《环球时报》看，它的火气主要是冲着德国媒体来的，按这篇报道的描述，书展主办者对中国官方可说是百依百顺，只有媒体在兴风作浪。而《环球时报》还很同情主办者，多次很欣赏地引述了主办者对“媒体捣乱”的不满。但是我后来看到，德国媒体对这件事的报道也很有意思，他们的火气主要并非冲着中国官方――中国官方要封杀异见人士在他们看来并不奇怪，他们恼火的是德国的书展主办者和有关当局居然也为某种利益“出卖了言论自由”，舆情汹汹都集中在后者身上。后来里布肯先生的一位朋友对我说，那些天里里布肯几乎精神崩溃，不停地给他打电话，语无伦次地念叨着：“坏了坏了”，“我完了”，“我要给钉在耻辱柱上了”……。显然，他说的是德国舆论（而非中国官方）要把他钉上“耻辱柱”。这位朋友说，其实里布肯当年曾是1968年左派学生运动的活跃者，后来也是所谓“文化左派 ”，即那些不一定主张社会革命，但强调文化多元，不一定要求改变西方，但也反对西方要改变别人，因而对“非西方”文化持同情态度的人。他对中国确无恶意，但“破坏言论自由”这个黑锅他是背不起的。<br />
　　于是中德两国报道给人的印象似乎是：德国舆论和中国公民戴晴等人为一方，中国有关部门和德国主办者暨有关当局为一方，为此事展开了博弈，这样的博弈能够看成是“中”“德”或“中”“西”的冲突吗？能够与“爱国主义”挂得上吗？<br />
<br />
会上所闻<br />
<br />
　　约20分钟后戴晴出了机场，守候在出口的大批记者一拥而上，“长枪短炮”闪光不停，喀嚓不断，戴晴面对记者用英语侃侃而谈。而里布肯先生则躲在一边有点尴尬，直到一些记者发现他，他才迎上前去，表示欢迎。这时贝岭也赶到了，里布肯先生却对他说：“我们没有安排您来”（他显然是指德国笔会再次邀请的只有戴晴），随即带着我、徐星和戴晴匆匆离开。在场者又是一片哗然，有德国朋友立即表示愿意接待贝岭去住，在媒体批评的压力下，主办者终于在次日把贝岭作为受邀请者接待了。<br />
　　第二天研讨会开始，由于这场会前风波已经沸沸扬扬，会场自然人气颇旺，气氛很不寻常。在这里我见到了“中方邀请”的诸位同仁，除了我们清华的李强教授，我认得的还有社科院的黄平、陆建德等先生。他们神色严肃，显然都已经处于准备应付冲突的状态。但是我向他们询问此事时，得到的回答却与《环球时报》所说的大相径庭。据我们这些同仁说，本来德方爱邀请谁就邀请谁，“中方”并不想管他们的闲事，但主办者却把他们自己发出的邀请书都寄到了新闻出版署，如果出版署转交，就等于“中方”也参与了邀请，“ 我们不能上这个套”，于是“中方”“客气地退回了（戴晴的）邀请书，请他们自便”。但德国媒体却“造谣”说我们阻止德方邀请他们两位，借此“煽动”对中国的攻击。云云。中国使馆的一位先生还在会上郑重宣布：中国方面“自始至终”从未干涉过德方的邀请行为，德国媒体如此造谣，实在太过分了。<br />
　　听了这话我不禁糊涂了。《环球时报》大标题上不就赫然写的是“研讨会突邀不速之客，中方组织者严词拒绝”吗？不是明确讲“德方不放弃邀请”中方就将抵制研讨会吗？而且都是用第一人称口气，并没有说是引述别人的话，这难道也是德国媒体造谣？<br />
　　我并未在会上提出这个问题，但私下向“中方”诸位同仁提出这个疑问，并问那个声称对此事“丝毫不能妥协”的赵俊杰先生是谁，是否也在“中方”代表之列？但是，“中方”同仁似乎都不知道《环球时报》的这篇报道，也不知道赵俊杰是谁。<br />
　　而且，如果“中方”的愤怒并不是因为“德方”邀请了谁，而是因为“德方”让“中方”转发邀请等于是迫使“中方”为这些邀请作背书，那么对于戴晴这可以理解，对于贝岭却又是怎么回事？因为贝岭在美国（我记得，他好像已经加入了美籍），他的邀请书是直接寄去的，并未经“中方”代转嘛。<br />
　　再有，据《环球时报》，关于中国阻止邀请两位“异见人士”的说法都是书展主办者说的，主办者以此向媒体解释他们不得不撤销邀请的苦衷。由于《环球时报》也表示要坚决阻止邀请，因此它的报道并未批评主办者，而只是批评向主办者施压的德国媒体。但是“中方”代表则不同，既然他们表示无意阻止邀请，只是抗议污蔑他们要阻止邀请的“谣言”，那么显然，放出这种“谣言”的就是主办者，而不是德国媒体，为什么他们也和《环球时报》一样，并不指责“造谣”的主办者，而只是激烈地指责“信谣”的德国媒体呢？<br />
<br />
会外思索<br />
<br />
　　我后来终于悟出了点什么。<br />
　　我想，当初“中方”（未必是高层，应该是处理此事的某个部门，比如新闻出版署）应当的确是极力阻止主办者邀请这两位的――主办者实在没有任何动机在这种事情上“造谣”，使自己陷入媒体的围攻。<br />
　　但当这事在德国犯了众怒，酿成轩然大波后，尤其在知道戴晴等人执意要去并且德国舆论普遍支持之后，更高层并不欣赏有关部门的这种做法，也不希望因此就抵制书展这个宣传中国“软实力”的好机会。否则，在美国的贝岭“中方”也许没有办法，在中国的戴晴如果官方决心阻止她成行，那是完全做得到的。谁都知道中国出境口岸是有“黑名单”的，有关方面只要把戴晴列进去，她自己买了机票也会被拦住。据戴晴自己说她过去也确实有过这样的遭遇。<br />
　　因此戴晴这次能够成行，应该是中国方面最终开了闸的。“中方”代表在会上关于无意干涉德方邀请的说法，大概就是出于这样一种比较开明的态度。然而风波已经酿成，总不好向德国舆论示弱吧？所以“德方”让“中方”转交邀请书这个程序问题就成了“中方”下台阶的一个理由，据此可以责怪德国主办者多事，批评媒体乱炒作，而中国可以得到一个开明的形象。<br />
　　但坏事的是：《环球时报》立功心切，竟把有关部门原来暗中施压的做法捅了出去，而且摆出一副封杀戴晴等人“丝毫不能妥协”的架势，却又没有与“中方”即时沟通，统一口径。《环球时报》不知道国内已经“妥协”，而“中方”也不知道《环球时报》如此报道，以至于出现如此尴尬局面。当然，尽管无论是我们自己，还是德国的许多媒体人（在机场给我报纸的就是一个德国记者）都已知道这件事，但在会上谁也没有提，我想这是因为我们国家变得更开明终究是件好事，就不必让我们的同仁难堪了吧。<br />
　　令人不可思议的倒是《环球时报》，到会议结束后的14日它又发表了一篇会议报道，作者还是那几个人，其中却说：德国媒体“散布中国施压、异见人士被拒绝的假消息”以“破坏气氛”！读后真令人倒吸一口凉气：作者竟然把自己三天前说过的话全忘了吗？“研讨会突邀不速之客，中方组织者严词拒绝”是谁散布的“假消息”？<br />
<br />
风波起伏<br />
<br />
　　由于会前的汹汹舆情已经弄得主办者灰头土脸，连法兰克福市当局也受到很大压力，而10多天后就是德国大选，当局所属的党派自然明白事情的严重，不想因此丢掉选票。因此12日的会一开场就变得十分高调，先是法兰克福市长对此事明确表态，她说自己历来坚持言论自由，不怕任何压力。过去书展就邀请过东欧的异见作家，今后也不会改变这一传统，还标榜自己曾接待过达赖喇嘛。然后书展的主办者和主要资助者也相继表态，高调欢迎戴晴和贝岭，对自己前段的“软弱”向公众道歉，并把戴晴、贝岭请上台发表感言。在整个这“欢迎、检讨、表态”过程中多次响起一片掌声，气氛十分热烈。<br />
　　这时坐在第一排的中国官方同仁坐不住了。他们原先其实已经接受了现实，在戴晴、贝岭与他们同坐一排时他们并没有像《环球时报》散布的“假消息”那样：“ 这两人出席，我们就离开”。但是会上如此高调地欢迎戴晴、贝岭而把他们撇在一边，的确让他们非常难堪。而且会议事先发下的议程中也确实没有这一“欢迎、检讨、表态”的环节。于是他们纷纷起身，全体退场，表示抗议。设身处地为他们想想，作为官方代表，也确实不得不这样。不过如果反过来为主办者想想，在前一段几乎被“钉在耻辱柱上”的尴尬中，在临近大选的关头，他们不这样“弥补过失”又怎么办？<br />
　　但是好在我们的官方同仁并未走远，书展主要资助者布斯先生前去“道了歉”后，他们很快又回到了会场，会议遂回到原定议程继续开始。我真是服了这书展的主办者，他们先向媒体道歉不该“屈从”中国，后向“中方” 道歉不该“屈从”媒体，真是老鼠进风箱两头受气，猪八戒照镜子里外不是人。然而也亏了他们这么两头作揖，这个气氛如此对立的研讨会居然还又开下去了。<br />
　　而且如果没有先前这沸沸扬扬的风波，书展中的这类活动本来并不会那么引起关注，这场风波倒是一个成功的广告行为了。平心而论，戴晴、贝岭两位本来在德国也没有那么大知名度，如果不是有先前的节外生枝，他们甚至都不一定会应邀前来，来了也不会形成那么大的舆论轰动。从这一点上讲，两位倒是应当感谢那“丝毫不能妥协”地必欲封杀他们的赵俊杰等辈了。<br />
<br />
我的发言<br />
<br />
　　会议虽然回到原先议程，但毕竟有了先前的一场，后来的发言者，尤其是“中方”发言者几乎都要离开原议题就这场风波说上几句，主要是驳斥“中国压制言论自由”之类的说法。记得好像是张蕴岭先生说：他们两位当然有权利在此发表自己的言论，但你们不该冷落我们而去专门捧他们。我当时就想：“他们两位有权发表自己的言论”？这话要是在国内说该多好啊！黄平先生还现身说法，他说他主编《读书》杂志14年，从来没有遇到来自权力部门的干涉。我听了好感动，我们国家真是进步了。但是，与黄平先生一起主编《读书》的汪晖先生不是曾抱怨，他们两位的主编之位被撤掉就是权势者做的手脚吗？两位主编的说法，我该信谁的？<br />
　　在第一阶段的发言者中，似乎只有我没有涉及刚才的风波。我是“德方”邀请的，但“中方”也代转了这份邀请，我对双方都心存谢意，但也没有义务加入争论去为哪一方辩护。这一阶段原定的议题是“中国的世界地位：中国对自己的认识和世界对中国的认识”，我也就直接进入了这个主题。<br />
　　我从刚刚调研所知的中资企业在东南亚的水电、矿业等开发活动谈起，讲到中资在劳工权益、对利益相对方（如被占地的当地农民）和当地公民社会的态度、以及环保意识等方面确实不如同在当地搞开发的一些西方公司，即使在老挝这么个政治背景与我国相似的国家也有这种反映。但是有人因此攻击中资在搞“殖民主义”，我是很不同意这种说法的。因为事实是：一方面中资在那里对利益相关各方做得不如一些发达国家的外资好，但是另一方面，中资做得确实已经比他们在国内对劳工、对被征地农民、对挑刺的NGO的做法要好多了，与在国内的做法相比，他们实在已经很进步了。而什么叫“殖民主义”？那就是像当年的西方一样，对自己的国民权利很尊重，对殖民地人民却很不好。然而，中资的情况恰恰相反，他们对海外投资地民众的“迁就”尽管不如西资，却几乎可以肯定要高于他们在国内的做法。这一点其实从中资受到的批评就可以证明。<br />
　　很多人批评中资使用从中国弄来的农民工而不肯雇佣本地人，这当然有违于增加本地就业的原则，受到批评可以理解。但是我对他们说：西方公司当然不会从他们国内弄一批白人到你们这里做重累脏险的苦力，为什么？只是因为要保证你们的就业机会？当然不是！这首先是因为白人不愿干这种活。西方人不愿干的活，西方公司让你们老挝人干，这是“殖民积习”；而你们不愿干的活，中资公司让中国的“农民工”去干，这又是什么积习呢？这当然不是殖民积习，但可以说是“低人权”积习。殖民积习是宗主国尊重本国公民的人权但不尊重他国人，而我们中国的强势者却相反，他们尊重你们的权利也许还不够，但绝对比尊重国内利益相关的弱势者要强多了！<br />
　　我因此表示很赞成扩大中资在海外的活动，这主要还不在于他们能为中国赚到多少钱，或者为当地作出什么贡献，而在于希望他们能够在海外学会尊重人权，从而反馈国内，能对中国人更好一些。如中铝公司在澳大利亚开发奥卢昆铝土矿，虽然得到了联邦政府和昆士兰州政府的大力促成，但仍不得不花费一年半时间与当地很少的土著人谈判土地租用问题。而在国内他们哪有这份耐心？中铝的“平等待人”在当地获得好评，可是在国内这个“好榜样”却不能宣传：国外的“工会陷阱”已经够让 “中资”头疼了，如果“奥卢昆经验”传入国内，拥有“五千年文明”的中国农民也学会了像澳洲那个小小的“原始部落”那样大模大样地与政府支持的“大鳄”漫天要价，那还了得？！<br />
<br />
“偏见”我见<br />
<br />
　　总的来讲，我还是从这次会议上感受到了中国的进步。虽然有波折，毕竟还是对戴晴他们开闸放行了。虽然“14年自主办刊未受干预”我不知道是不是已经如此，但至少我们的官方同仁认为应该如此。我们的官方同仁说中国已有言论自由，但愿他们说的能逐渐变成现实。我们的同仁大都也承认中国“纵向有进步，横向有差距”，看到差距就有了进一步前进的可能。<br />
　　只是我不明白，为什么我们的官方同仁对此间的政府、公司（主办者）都还能友善，唯独与此间的民间媒体如此对立？<br />
　　是因为如意识形态所说：西方媒体被金钱控制，只是资本家老板们的喉舌？如果是这样，“老板喉舌”的可恶不就来自“老板”的可恶吗？为什么我们对“老板们”本身反倒更好？<br />
　　是因为这些媒体散布了“假消息”或“谣言”？但就以这场风波为例，平心而论，我们自己的《环球时报》散布的“假消息”，包括对“中方”不利的“假消息”难道还少吗？<br />
　　是因为这些媒体对中国有“偏见”？的确如此。而且甚至可以说，西方媒体的一些“偏见”确实与“殖民主义积习”有关。因为“殖民积习”如前所述，就是对自己同胞要比对外人更好，或者说对外人比对自己同胞更坏。西方人过去的确有这个毛病，现在也还没完全改掉。他们以这种眼光看我们，看到我们有些人（比如那位要求“毫不妥协”地封杀戴晴的先生）对同胞如此杀气腾腾，就不禁会想到：“他们对自己的同胞都是如此的坏，如果这种人强大起来，他们对我们这些外人又会坏到什么程度呢？”老实说，所谓的“中国威胁论”除了某些真真假假的事例外，主要就是出于这样的一种心理“偏见”。<br />
　　当然我们可以说服他们：我们和你们相反，我们这种“文化”对外人比对同胞更好，或者说对同胞比对外人更坏，我们不准戴晴讲话，但不会不准你们讲话，你们不能带着你们的偏见看我们。但我想，我们这种博大精深的“文化”，这些夷狄恐怕是理解不了的。如果我们为此就大动肝火，那就更把这些夷狄吓坏了，他们的“偏见”不也就更深了吗？<br />
　　更何况说透了：我们对这些媒体大动肝火又有什么用呢？说的极端些，对他们的政府动肝火，我们可以断交甚至宣战，对他们的老板动肝火，我们可以进行贸易制裁，但他们的媒体何求于我，我们能拿他们奈何？如果说我们是新闻全球开放的，正如那位老默多克曾经希望的那样，他们想到我们这里来发行，来扩大市场份额，或许还会顺着我们说话。现在我们根本是不许他们进来的，他们完全以我们以外的人为受众，怎么会在乎我们的肝火？我们对自己的媒体发威，可以审查，可以撤人，乃至停刊整顿、责令关张，对他们的媒体我们能怎么样？我们能对他们搞新闻审查？能让他们停刊整顿？他们连自己的政府都不怕，怎么会在乎我们动肝火呢？说损一点，我们的肝火，不恰恰是给他们做的免费广告吗？<br />
　　所以我们动肝火是没用的。可取的做法是我们以行动来逐渐打消他们的偏见。不仅对外人要好（这我们似乎不难做到），更要对自己的同胞好，尊重自己同胞的人权。我们不要以为这只是自己的“内政”，“内政”只能对政府而言，无法对媒体和舆论。正如我们不能跑到美国去代替美国政府废除资本主义、但我们的媒体却完全可以抨击“资本主义的罪恶”一样，他们的政府管不了我们的“内政”，但他们的媒体对我们的事“说三道四”，无论是我们还是他们的政府都是管不了的。<br />
　　可以肯定地说，尽管新闻的真实性与公正性是媒体的生命，我们完全应该有这样的要求，但是事实上人们的价值偏好客观存在，“偏见”这个东西就很难完全避免。而且它与新闻自由与否无关。被管制的媒体有偏见，自由的媒体同样有偏见。新闻自由只是可以防止“偏见一元化”，即防止那种只能朝这“偏”、不能朝那“偏”、只准我“偏”不准你“偏”的情形出现，防止某种一面倒的、受操控的甚至是被动员的“偏见”诱发全社会的歇斯底里。“偏见”一旦多元化，那就有可能“兼听则明”，使受众在各种“偏见”的平衡中找到真实。“偏见”一旦竞争化，那些“偏见”较少、更有公信力的媒体就有可能胜出，而那些不讲公信、一味“偏见”的媒体会被淘汰。<br />
　　所以我们对某个具体的不实新闻当然要出来纠正，以免以讹传讹，但对“偏见”存在的一般现实应当持有平常心。只要我们把自己的事情做好，对我们的“偏见”必然逐渐减少，或者会多元化地出现“亲华偏见”对“反华偏见”的平衡。如果我们又有足够的自信来实行新闻自由和媒体市场的开放，那些媒体更可能为了顺应我们受众的胃口而克制令我们受众反感的偏见了。因此我很高兴地看到这次研讨会上我们官方同仁能跟戴晴、贝岭同室讨论问题，能否定那种 “毫不妥协”地封杀“异见”的赵俊杰式主张。如果在国内也能如此，那么我们的进步将是任何“偏见”也遮蔽不了的。<br />
