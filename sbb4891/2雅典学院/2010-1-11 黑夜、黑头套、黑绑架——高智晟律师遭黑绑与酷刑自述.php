<br />
――高智晟律师自述遭绑架经历<br />
<br />
我费尽周折终会面世的文字，将撕去今日中国许多东西的人相，露出“执政者”那超乎常人想像的心肠本色。当然，这些文字也是将给今天共产党在全世界的那些 “好朋友”、“好伙伴”带来少许不快、甚而至于难为情――（如果）这些“好朋友”、“好伙伴”们内心对道德及人类良知价值还存有些敬畏的话。今天，暴富起来的共产党，不仅在全球有了越来越多的“好朋友”、“好伙伴；”而且把“中国是一个法治国家”这种颠倒黑白的口号喊得气壮如牛。对中华民族人权进步事业而言，这两者无一不是灾难性的。<br />
<br />
2007年9月21日夜20点左右，当局口头通知说让我去接受例行的改造思想谈话。行在路上，我发现较往常比有了些异样，平时贴身跟踪的秘密警察们拉开了较远的距离。行至一拐角处时，迎面扑来六、七名陌生人。我的背后脖胫处被猛然一击，眼前感到整个地面飞速向我砸来，但我并未昏迷。接下来，感到有人纠起我的头发，迅速套上了黑头套，被架上了一辆凭感觉是两侧面对面置有座椅而中间无椅的车上。我被压迫爬在中间，右侧脸着地，感到有一只大皮鞋猛然踩压在我的脸上。多只手开始在我身上忙禄，由于他们对我一家的绑架频繁，故而照例在我身上未搜得对他们有价值的东西。但我感觉到了此次与以往绑架的不同。<br />
<br />
绑架者抽下了我的皮带将我反绑，我爬在车中间，估计着有不低于四个人的脚踏在我的身上。大约四十分钟左右，我被拖下了车站立着，裤子已掉至脚脖上的我被推搡着进了一间房屋，此前一直没有任何说话的声音。我的头套猛然间被人扯下，眼前一亮的同时，辱骂和击打开始了。<br />
“高智晟，我操你妈的，你丫的今天死期到啦，哥几个，先给丫的来点狠的，往死里揍丫的”，一个头目咬呀切齿吼叫道。这时，四个人手执电警棍在我头上、身上猛力击打，房间里只剩下击打声和紧张的喘气声。我被打的爬在地上，浑身抖动不止。“别他妈让丫的歇了”，王姓头目吼道（后来得知之姓王）。这时，一名个头一米九以上的大汉抓住头发将我纠起，王姓头目扑过来疯狂抽打我的脸部，“操你妈，高智晟，你丫的也配他妈穿一身黑衣服，你丫是老大呀，给丫的扒了”。我迅速被撕的一丝不剩。“让丫的跪下”，随着王姓头目的一声吼叫，后小腿被人猛击两下，我被打扑跪在地上。大个子继续纠住我的头发迫逼我抬头看着他们的头目。<br />
<br />
这时，我看到房子里一共有五人，四人手持电警棍，一人手持我的腰带。“你丫的听着，今天几位大爷不要别的，就要你生不如死，高智晟我也实话告诉你，现在已不再是你和政府之间的事啦，现在他妈的已经完全变成个人之间的事啦，你丫的低头看一看，现在地上可一滴水都没有，呆会儿地上的水就会没脚脖儿，你他妈一会儿就会明白这水从哪里来”。王姓头目在说这些话的时候开始电击我的脸部和上身。“来，给他丫的上第二道菜”，王头目话落，四支电警棍开始电击我，我感到所击之处，五脏六腑、浑身肌肉像自顾躲避似的在皮下急速跳躲。我痛苦的满地打滚，当王姓头目开始电击我的生殖器时，我向他求饶过。我的求饶换来的是一片大笑和更加疯狂的折磨。王姓头目四次电击我的生殖器，一边电击，一边狂叫不止。数小时后，我不再有求饶的力量，也不再有力量躲避，但我的头脑异常的清醒。我感到在电击时我的身体抖动的非常剧烈，清楚地感到抖动的四肢溅起的水花。这是我在几小时里流出的汗水，我这时才明白“呆会儿地上的水就会没脚脖儿”之意。<br />
<br />
这种深更半夜折磨人的活计对折磨者似乎也不轻松。天快亮时，他们有三人离开房间。“给丫的上下一道菜，呆会来换你们哥俩”。王姓头目示意留下的俩人将一把椅子搬至房中间，将我架起来坐在上面，这时，其中一人嘴里刁上了五支烟，用火点着后猛吸几口，另一人站在后面用力抓住我的头发，压迫我低下了头，另一人开始用那五支烟熏我的鼻子和眼晴，这样反复多次。他们做的很认真，也很有耐心。待到后来，我除了能偶然感到泪水流下来滴在大腿上的感觉外，已完全不再在乎眼前这俩个人的忙碌和我有什么联系。<br />
<br />
过了约两小时左右，进来两人换下辛苦用烟熏我的那俩位。我的眼睛肿胀得什么也看不清。新进来者开口说话了：“高智晟，耳朵现在还能听到吧？算你<br />
点背，这帮人都是长年打黑除恶的，出手狠着呢。这是这次上面专门精心给你挑选的，我是谁你听出来了没有？我姓江（音），你去年刚出来时跟你去过新疆”。 “是山东篷莱的那位吗？”我说。“对，你记忆不错，我说过，你早晚还要进来，上次去新疆我看你那个样子，我就知道你再次进来是早晚的事，你看你在警察跟前目空一切的德性，不让你再进来长点记性能行吗？给美国国会写信，你看你那一付汉奸德性，美国主子能给你什么？美国国会算个鸟。这是在中国，这是共产党的天下，你算个屁，要你的命还不像踩死只蚂蚁一样？不明白这点还出来混，你要敢再写那些狗屁文章，政府就得表明个态度，这一晚上你该明白了吧？”江不紧不慢地说。<br />
<br />
“你们这样用黑帮手段残忍地对待一个纳税人，今后有何颜面面对十几亿国人？”我问他。“你就是个挨打的东西，你心里比谁都明白，在中国纳税人算个狗屁，别他妈口口声声纳税人纳税人的”，江正说着，这时又有人走进来的声音。“甭他妈的跟他练嘴，给丫的来实在的”，我听出来者是王姓头目。“高智晟，你这几位大爷给你准备了‘十二道菜’，昨晚才给你伺候了三道，大爷我就不爱罗嗦，后面还要让你丫的吃屎喝尿，还要拿签子捅丫的“灯”（后来才明白是指生殖器）。你丫的不是说共产党用酷刑吗，这回让你丫的全见识一遍。对法轮功酷刑折磨，不错，一点都不假，我们对付你的这十二套就从法轮功那儿练过来的，实话给你说，爷我也不怕你再写，你能活着出去的可能性没有啦！把你弄死，让你丫的尸体都找不着。我他妈想起来气就不打一处来，你一个臭外地人，你丫的在北京张狂什么呀，哥几个再他妈练丫的”。<br />
<br />
在接下来几个小时的折磨中，我出现了断断续续的昏迷，这种昏迷可能与长时间的出汗缺水及饥饿有关。我光着身子躺在冰冷的地板上，神志像过山车一样起伏不断。中间感到数次有人剥开我的眼皮用光晃我的眼睛，像是在检查我是否还活着。每至清醒时，我闻到的全是尿臭味。我的脸上、鼻孔里、头发里，全是尿水。显然，不知何时，有人在我头上、脸上撒了尿。<br />
<br />
这样的折磨持续到第三天下午时，我至今不知当时那里来的巨大力量，我挣脱他们，一边大喊天予和格格的名字，一边猛地撞向桌子。我当时大叫孩子名字的声音今天回想起来都感到毛骨悚然，那喊声极其凄远及陌生。但自杀未能成功。感谢全能的上帝，是他救了我，我真切地感到是神拖住了我。我的眼睛撞得流血不止，我倒在地上，至少有三个人坐在我的身上，其中一人坐在我的脸上。他们大笑不止，说我拿死来吓唬他们是提着耗子吓唬猫，这样的事他们见得太多啦。<br />
<br />
他们一直继续残忍地折磨我到天黑，我的眼睛什么也看不见。我能听得出，折磨我的人轮换着吃完饭后聚齐。其中一人走至我面前抓住头发将我纠站起来问：“高智晟，饿不饿？丫的说实话”。答曰：“饿得快要不行啦”。“想不想吃饭！得说实话”，又问。我又答曰“想吃”。话落，不低于十几个耳光的一阵巴掌打得我一头栽倒在地。有一只脚踩在我的胸上，我的下巴被电警棍猛击一下，打得我疼得大叫。这时，有一根电警棍塞到我的嘴里，骂声也一同而至：“你丫的头发怎么这么不经纠？看看丫的这张嘴和别人有什么不一样的，还不是要吃饭吗？饿，丫的配吗？”但电警棍塞进嘴里后并没有用电击我。正不知所故，王姓头目发话：“高智晟，知道为什么没废掉丫的嘴吗？今晚上几位大爷得让你说上一晚上。甭跟大爷们扯别的，就说你搞女人的事。说没有不行，说少了不行，说的不详细也不行，说得越详细越好，几位大爷就好这个。大爷们吃饱喝足了，白天也睡够了，你就开始讲吧”。“操你妈，你丫的怎么不说呀，丫的欠揍，哥儿几个上，王头目大叫”。大约三支电警棍开始电击我，我毫无尊严地满地打滚。<br />
<br />
十几分钟后，我浑身痉挛抖动得无法停下来。我的确求了饶：“不是不说，是没有”，我的声音变得很吓人。“哥儿几个，怎么搞得呀，伺候了几天怎么把丫的伺候傻了？给丫的捅捅‘灯’（生殖器），看丫的说不说”。接着，我被架着跪在地上，他们用牙签捅我的生殖器。我至今无法用语言述清当时无助的痛苦与绝望。在那里，人的的语言，人类的感情没有了丝毫力量。最后我编了先后与四名女子“私通”，并在一次一次的折磨中“详细”描述了与这些女人“发生性关系”的过程。直到无亮，我被抓着手在这样的笔录上签了名，按了手印。<br />
<br />
“半年内让丫的变成臭狗屎。这事儿整出去，你身边的那些人会像饿狗碰了一嘴新鲜屎一样高兴的。”王头目大声说。（我出来后得知，就在第二天，孙＊处长即把他们“掌握的”我乱搞男女关系“实情”告诉了我的妻子。 耿和告诉之: 其一,在给高智晟的为人下结论方面自己不需要政府帮助;其二,若过去纵有其事,在自己眼里,他实在还是那个写三封公开信的高智晟）。<br />
<br />
经这次折磨后，我几乎时常处在没有知觉的状态中，更多的是没有了时间知觉。不知过了多久，一群人正准备再次施刑时，突然进来人大声喝斥了他们，让他们都滚出去。我能听得出，来者是市局的一位副局长，此前我多次见过之。至少在我认知的层面上对之有好感，人较为开明、直率，对我和我全家有过一些保护。当时我的眼睛不能睁开，但我整个人已体无完肤，面目全非。听得出他也很愤怒，找了医生给我作了检查，说他也很震惊，但说这绝不代表党和政府的意思。<br />
我问他谁的意思能如此无法无天，之无以对。期间，我要求送我进监狱，或送我回家，他没有作答。最后他将折磨我的人叫进来声斥了一阵，命他们给我买衣服穿，晚上必须给我提供被子，必须给我饭吃。并答应尽全力为我去争取或回家，或进监狱。<br />
<br />
这位局长一离开，王姓头目对我破口大骂：“高智晟，你他妈现在还在作梦想进监狱，美死你，今后你再甭想进监狱，只要共产党还在，你就再也没有进监狱的机会，什么时候也别想”。<br />
<br />
当天晚上，我又被套上黑头套昏沉沉地被架到另一个不知名的地方，在那里又被他们无休止地折磨了十几天后。有一天，我突然又被套上黑头套后，被人架着按着头，九十度弯腰跑步至一辆车上。上了车，我的头被人按低至我的裆部，路上一个多小时，真是生不如死的痛苦境地。到了地方后约一小时才取下黑头套。对我实施肉体折磨的五人中不见了四人，换来的是出狱后贴身监督我“改造”的那群秘密警察。对我肉体的折磨至此而止，而精神折磨一直持续。<br />
<br />
我被告知要开“十七大”了，在这里等候上面的处理意见。期间一些官员时有来访，变得温和了许些，也开始允许我洗脸刷牙了。也有官员提出能否用我的写作技术 “骂骂法轮功，价钱随你开口，知道你有这能力”。我明确告诉来者，“这不只是一个纯技术问题，这是一个困难的伦理问题。” 到后来一看没有动静，又来说“写法轮功的文章困难的话，也可以表扬表扬政府嘛，多少钱都不成问题。”<br />
最后是“写点东西说你出狱后政府对你全家很好，是受了法轮功和胡佳等人的蛊惑才一时糊涂写了给美国国会的公开信的，要不然，这什么时候是个尽头。你就不能可怜可怜你的妻子、孩子吗？后来作为交换，我写了一份说政府对我全家关心倍至，是受了法轮功和胡佳的蛊惑我才写给美国国会公开信的材料。<br />
回家前，我又被带到西安给胡佳打了一次电话。大约是中秋节夜里，此前因耿和的以自杀抗争，当局让我打了一次劝慰电话。通话内容都是由当局设计好的（我回来后得知，耿和所说的内容也是设计好的）。当局还录了相（当时我还有一只眼睛无法睁开，录相中逼我说是自伤的）。<br />
<br />
十一月中旬回到家得知，家中部分财产再次被抄，这次抄家连一个字的纸条都没有。<br />
<br />
我在这五十多天里遭遇到的肉体及精神折磨所谓骇人听闻。期间有过许多奇异的感觉，诸如：有时候能真真切切地听到死，有时又能真真切切地听到生。到第十二、三天后我完全睁开眼时，我发现全身的外表变得很可怕，周身没有一点正常的皮肤。皮肤完全呈重度乌黑色。被绑架期间，我每天“吃饭”的经历，定会让那些在纸上操英雄主义枪法的义士们大跌眼球。<br />
<br />
每至饿致眼冒金星时，他们会拿出馒头来.每唱一遍《共产党好》、《社会主义好》、《没有共产党就没有新中国》，即可得一个馒头。我当时的心理底线是除非万不得已即设法活下去。死对我的妻子和两个孩子太过于残酷，但绝不脏污灵魄。在那样野蛮的氛围里，人性，人的尊严是毫无力量的。如果你不唱，你不但会被饥饿折磨，而且他们会无休止地折磨你。但当他们用同样的手段逼我写批法轮功的文字时，即未能如他们所愿。<br />
<br />
但以这种方法让我在写有这次政府没有绑架我，也没有酷刑折磨我，政府一直对全家关爱倍至的笔录上签名时，我是作了妥胁的。<br />
<br />
而在这五十多天中间，还发生了一些为人类政府记录史所不耻的肮脏过程，更能使人们看到，今天共产党的领导人，为了保卫非法的垄断权力，在反人性的恶行方面会走得多远！但这些肮脏的过程我不愿再提及、或许会永远如是。在每次的折磨我的过程中，他们都会反复威胁说，如果将来有一天，把这次的经历说出去，下次就会在我的妻子，孩子面前折磨我。<br />
<br />
大个子每一次都抓住我的头发告诉我：“把这次的事说出去了，你丫的死期就到了，几位大爷随时找你败火”。这样的警告不知被重复了多少次。这些东西的心里也清楚，这样的残忍暴行并不十分伟大光荣正确。<br />
<br />
最后，我还想再说一句不太讨人欢颜的话，即我想提醒今天共产党在全球的那些“好朋友”、“好伙伴”们：共产党对国内人民愈发蛮横及冷酷的十足底气，是被我们和你们一同给惯出来的。<br />
<br />
<br />
2007年11月28日于被警察围困的北京家中<br />
<br />
