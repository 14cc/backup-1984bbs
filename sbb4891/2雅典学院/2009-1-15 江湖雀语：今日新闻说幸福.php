<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; 江湖雀语：今日新闻说幸福<br />
<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;&#8226;2009年，国民收入要向居民收入倾斜。只追求GDP不能让个人取得幸福感。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;&#8226;绵阳有人在银行取款机里取一千元，有九百元是假币。上海也有银行发放了假币。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;&#8226;大学生就业难，有高层人士建议设立“学士后”制度，让大学生零工资工作两年。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;&#8226;南方成品油价下降，北方上涨。原因：南方成品油走私猖獗。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;&#8226;中石油、中石化大量成品油出口，宁可优惠国外，不肯便宜国内。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;&#8226;黄牛的火车票，是他们反复排队所得。网友拍到的疑似售票员倒票的视屏是误会。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;&#8226;春运三件宝：充饥的方便面、应急的尿不湿和随地睡觉用的旧报纸。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;&#8226;钢城马鞍山打造“中国洗浴中心”。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;&#8226;贵州凯里挪用教育经费宣传“凯里教育开放三十年的伟大成果”。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;&#8226;北京政府规定：居委会有权撤销住宅区业主委员会的决定。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;&#8226;打工仔回苏北大多成了小老板。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;&#8226;剧毒物氰化钾毒死的狗卖到韩国料理店，食用后将伤人。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;&#8226;贵州农村家庭吵架，村干部上门强制摆“劝和饭”劝解，每次耗费五百到一千元。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;&#8226;MBA考试前十题作废。原因：校对错误。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;&#8226;我国出版界模仿、抄袭成风，近期出《奥巴马传》二十余种，其中原创仅有两种。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;&#8226;上海将耗资244.8亿打造“迪斯尼乐园”，内地80%游客将涌入上海。<br />
