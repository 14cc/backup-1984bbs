<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;我们老百姓到底幸福吗？这个问题也许思考的人不会太多，更多的人思考的还是生存问题，因此，提高居民收入光在新闻节目里嚷嚷是不够的，更何况说得还不真实。因为，只追求GDP是有人幸福的，那就是追求政绩好升官的领导们，不然他们才不会追求呢！<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;不顾老百姓的幸福，只追求自己的乌纱帽，这问题无疑是出在制度上。同样，银行发放假币，问题也出在制度上。没有透明的政治，民主的监督，甚至不让人批评，利之所在能不令人智昏吗？要让大学生白打工的什么“学士后”，也是这类发昏的主意。试想：有人白打工，谁还会出钱雇人呢？这正常的就业，岂不永远没有了可能性？<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;制度问题在经济上无过于可怕的垄断：石油垄断了，就不肯与国际接轨了。如果跟人家的价格一样，走私是不可能的，也不会去便宜了人家倒宰自己。该追问的是：为什么我们的石油是垄断的呢？其受获益者，他们的背景是什么呢？这就像对倒火车票的黄牛也要追究背景一样。谁都知道靠自己排队是假的，不然，为什么黄牛能排到票，普通人却不能呢？<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;普通老百姓的幸福，大多只能寄托在方便面、尿不湿和旧报纸上，但是，当官的幸福就可以建立在鼓捣“中国洗浴中心”上；宣传“伟大成果”上；以及侵犯老百姓管理自己住宅的权力上；或者，干脆把幸福感建立在睁着眼睛说瞎话上。比如，管失业的农民工烤几只白薯上街买之类，叫做：当小老板。不知道，他们把与自己勾结的，卖毒狗肉的老板叫什么，是拉动内需的爱国人士吗？于是，吃老百姓“劝和饭”的干部幸福了；印错考题的也无所谓了；出跟风书的简直就是有功了，因为，这样的书有利于统一思想的，平庸型社会安定。<br />
