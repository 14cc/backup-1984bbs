																											<div id="postmessage_693938" class="t_msgfont">春秋<br />
<br />
&nbsp; &nbsp; 刘晓波是资深的民运人士，而且由于其坚决不出国和鲜明的非暴力立场，使之在民运阵营中显得十分的另类。2009年底，他曾发表这样的宣言：《我没有敌人》。这篇文章中，他赞扬和尊敬自己的对手，称赞他们和平、理性，时时流露善意，并且对比过去的经历，肯定他们尊重在押人员的人格和权利基础上表现出来的人性化管理的进步，甚至感谢他们对他的人道待遇。他还这样解释自己的立场：&quot;因为仇恨会腐蚀一个人的智慧和良知，敌人意识将毒化一个民族的精神，煽动起你死我活的残酷斗争，毁掉一个社会的宽容和人性……&quot;&quot;我愿意自己能够超越个人的遭遇来看待国家的发展和社会的变化……&quot;。因此这样一个人物出现在《时代》周刊&quot;年度百名全球最具影响力人物&quot;的200人候选名单里,并不令人意外。毕竟民主和非暴力都符合西方的政治正确。<br />
&nbsp; &nbsp; 然而，在200人候选名单上网络投票中曾经排位第八的刘晓波，却出人意料的落选。外界一片哗然之际也纷纷猜测个中原因。是中国政府施加影响？还是《时代》周刊根本就无视网络民主、民意？然而真相是如此的令人难以置信：造成刘晓波淘汰出局的始作俑者竟然是他的民运同道。 <br />
&nbsp; &nbsp; 在《时代》周刊网投日趋激烈之际，&quot;独立中文笔会&quot;的蔡咏梅在网上呼吁笔会成员下载专为刘晓波设计的投票软件，以便为刘晓波不间断的连续重复投票，通过作弊的方式让刘晓波的票数在短时间内飙升。<br />
只是这种低劣的手段自然无法瞒天过海，不仅再次重创海外民运几乎丧失殆尽的公信力，损害了刘晓波的声誉，而且也最终导致了他的不光采出局。当然，刘晓波曾担任两任&quot;独立中文笔会&quot;主席，身为其中一员的蔡咏梅为之拉票并无不当。然而其手段却走向了民主的反面。网络投票也是民主的形式之一，对于民运来说，不难理解到其神圣性和严肃性。但是他们却选择了欺骗。一个以民主为追求的群体却在民主实践中上演骗票戏码，这岂不是海外民运的自我否定？<br />
&nbsp; &nbsp; 不过如果了解海外民运丑陋不堪的内斗史，对蔡咏梅的动机恐怕还要再多一种解读。刘晓波由于发起零八宪章和被判刑，声望雀起，并入选时代年度人物百人榜。根据民运惯例，定有黑箭射向刘晓波，将之拉下马。只是没想到竟然是这种方式。不过刘晓波在担任主席之时，不也是大搞&quot;路线斗争&quot;？特别是&quot;开除高寒案&quot;，致使忍无可忍的高寒，将中文笔会告上法庭，给已经乱轰轰的民运场再添一场闹剧。<br />
&nbsp; &nbsp;海外民运走到今天，早已声名狼籍。长期以来，民运争名夺利中不断走向颓败，到今天竟至沦为混饭吃、讨生活的乌合之众，为抢资源争地盘，早已没有底线，极尽搬弄是非、颠倒黑白、党同伐异、煽动蛊惑之能事。然而此次蔡咏梅在民主选举中利用虚假手段欺世盗名、沽名钓誉，却算是一个创举，显示海外民运又恶质化到一个新的阶段。遥想当年王丹来到美国，在看破海外民运真相后，召开记者会，公开宣布退出，并声称海外民运已经失败。宣布退出的还有吾尔凯希，他甚至干脆离开美国，远走台湾，以示决绝。<br />
&nbsp; &nbsp; 现在海外民运最急需的是反思和自我救赎。而蔡咏梅之举显示海外民运已经失去了反思的能力，失去了自我救赎的可能性。或者反过来说，正是由于对海外民运前途的信心丧失，才使得蔡咏梅突破底线，无顾忌。但不管如何，这都再次印证了海外民运名存实亡的凄惨现状。<br />
&nbsp; &nbsp; 中国民主之路如何走，是一个百年课题。海外民运的实践及现状却不由的再次令人深思：民主中国的出路到底在哪里？试问民运及尚在狱中的刘晓波，谁能回答的了？<br />
