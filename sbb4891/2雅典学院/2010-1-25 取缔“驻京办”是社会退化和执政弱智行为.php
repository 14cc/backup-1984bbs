													<h2>取缔“驻京办”是社会退化和执政弱智行为</h2>
						
						
																											<div id="postmessage_449894" class="t_msgfont">近几日，我参加了多场科普、文艺类的活动，从咖啡厅小派对到百人大讲座、从线下聚会到艺术画廊，总是能够发现英国大使馆文化教育处的身影。无论是“科学松鼠会”的科普讲座还是搜狐网主办的新春联谊，他们总是能适时适景的出现，分享某本新出的好书、或推荐某部纪录片。 <br />
<br />
这是一个推广文化的外国派驻机构，也可谓是“驻京办”。但和他们相比，我们国内由各级地方政府派驻的驻京办，却已经沦落到过街老鼠、人人喊打的地步。根据最新的报道，有关主管部门出台了一个关于加强和规范各地政府驻京办事机构管理政策，在未来的6个月内，数千家驻京办将被撤销完毕。<br />
<br />
驻京办存在很大问题，但是不是应当这样简单的一关了之，却依然值得商榷。道理显然是浅而易见的，假若我们察觉并判断出，驻京办已经是一个庞大而污浊的病瘤，那么最应该做的，应当是去治理病瘤得以产生的不健康肌体，而不是简单地割去病瘤本身。<br />
<br />
只要孕育驻京办的一整套行政体制不改变，那么驻京办这一现象必然会依旧存在，只不过是摘下了牌子、没有了编制。有活动能力的人，比如那些驻京办主任们，依然会利用个人的人脉资源和社交手腕，去帮助家乡完成某些公关事务。公款吃喝和灰色往来这些问题，一样存在。这些开销只不过是换了个发票的抬头，以另一种方式入账报销。<br />
<br />
但这种形式，相比于驻京办目前的组织结构和产权归属，实际上却是一种大退步。恢复个人身份的“中间人”或“掮客”们，必然会得益于以私人形式游说、公关的种种便利空间，这只会带来更严重的隐蔽化、私人化的交易，使得政府对外派机构的监管更为困难，也更容易滋生贪污受贿的违纪案件。<br />
<br />
驻京办的产生与畸形发展，源于中国政府惯有的中央集权文化。诸多有效资源都集中于中央政府和各大部委之手，自然会引起地方政府努力寻求“终南捷径”，当这种捷径成为公开的潜规则之后，管理失控是必然的。<br />
<br />
潜规则和寻租空间即在，所谓取缔也只是一厢之愿。实际上，驻京办今天的尴尬处境以及滋生的种种弊端，皆源于自身的错误定位和职能偏差。驻京办的出路，也就在于调整定位和职能。我们亟需的不是现在这种以酒肉、权钱为媒介迎来送往的驻京办，而是一种新的承担“社会工作”职能的驻京机构。<br />
<br />
中国目前并无成熟的社会结构，在很些方面，都存在着配置失衡和功能缺失。我们日日必谈“社会”，但“社会”是什么，却谁也说不清。以至于到今天，在政府和民众之间，实质性缺乏“社会”这一功能体系的存在，其结果便是政府管理职能的无限制外延扩大、以及个人和政府施政的直接行为冲突。<br />
<br />
譬如，发生在企业的职工和资方的薪酬冲突，原本是一个双方平等商讨和调整的市场行为。但由于没有工会、没有劳工权益组织，使得个体在博弈中处于绝对弱势地位并遭受不公损失。这时候，劳动者会将个体遭遇归罪于政府的监管缺位，并由此抵制政府的执政行为。<br />
<br />
历史上，政府的管理职能由皇室、内阁和官僚阶层整体完成，而在社会治理、民众沟通等环节，则交由宗族、乡绅等民间社会组织或人员完成。但这一演练千年的模式，在后来的划分社会阶级的屡次冲击中，受到了较大影响，造成了今天的中国人“有家庭无社会”的意识不足，也带来了个体和政府直接对抗的隐忧。<br />
<br />
结构失衡这个缺陷的不良后果，至今依然存在，并已经在诸多方面体现出日益严重的问题。要解决这个问题，三年、五年都不够。驻京办作为拥有先天优势的派驻机构，不是没事做，而是该做的不做。社会的发展亟需强化社会职能，民工欠薪、劳工保护、流浪人群管理、招商引资、文化推广、劳务输出等等都亟需建立完善、高效的机制和平台，都需要投入大量人力物力，都需要专门的机构去管理维护。因此，问题的焦点并不在于驻京办本身的对与错，也不是粗暴地考虑一个官僚机构的保留或取缔，而是一个角色和职能的转变：如果驻京办不是吃喝玩乐的官老爷，而是勤勤恳恳做实事的社会工作者，一切问题都是另一个答案。</div>

							
							
							
							
