													<h2>什么是概念</h2>
						
						
																											<div id="postmessage_340028" class="t_msgfont">作者：新月<br />
<br />
注意下面我的两句话：1）“所有天鹅都是白色的”；2）“天鹅是白色的动物”。<br />
<br />
第一句话如果有人对你说，你怎么反驳呢？答案很简单，你可以说“这个世界上有黑色的天鹅”，最好是顺手拎一只出来，当然别被动物保护组织看见就好。说这个什么意思啊？就是说，这第一句话叫做“经验性命题”，那么我们就能用经验来反驳。拿小学生的道理就是“同种单位的数量才能运算”。<br />
<br />
第二句话如果你继续用上面的话来反驳，那么就是错误的，因为我会说，“那种黑色的动物就不是天鹅”。看见了吗？规定是不能用经验来反驳的。同时，继续看“这个世界上有黑色的天鹅”这句话，其实这句话中暗含了一个关于“天鹅”的概念的规定，那么实际上这句话也是用一个规定反驳另一个规定，这是显然错误的。<br />
<br />
详细说来，比方说，我规定“理性人就是会低买高卖的人”，这就是一个规定，你如果反驳说“错，我认为理性人是……”。这显然不叫反驳，而是你新的定义。那么你要说定义是不是能胡乱来呢？当然不是，规定的问题要从规定的语境中来判断。比方说我规定“理性人就是会低买高卖的人”，那么你要看这个规定在我的思路中是不是恰到好处。有些人很无知，自己规定了一个定义，照理说，没问题，规定就规定吧。结果自己在行文中不知不觉扩大了外延或者更改了内涵，这就可以说这个规定是“不适当的”，而不是“错误的”。概念无对错可言。<br />
<br />
