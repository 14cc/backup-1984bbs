																											<div id="postmessage_496739" class="t_msgfont">是谁下令开枪的、到底杀了多少人？<br />
杨恒均<br />
<br />
<a href="http://yanghengjun.blog.hexun.com/33470650_d.html" target="_blank">http://yanghengjun.blog.hexun.com/33470650_d.html</a><br />
<br />
由于接到举报有人走私烟卷，相当于城管的烟酒专卖局缉私队出动，结果，只抓到一位售卖走私烟的小摊贩。这位40岁女摊贩看到自己的养家活口的香烟摊被没收了，跪在地上哭着哀求执法人员，结果却被粗暴的缉私队员用枪托打破了头，鲜血流得满脸都是，让围观的群众忍无可忍，走向街头、游行示威，最后发展到围攻政府……<br />
<br />
更没有想到的是，由于官员的腐败和无能，加上国民党独裁政府的痼疾，这样一个群体发泄不满的事件，竟然引来政府调兵遣将，对群众使用真子弹进行了屠杀……<br />
<br />
这就是1947年2月28日发生在台湾的“二二八事件”。是谁下令开枪的？事件中到底有多少无辜者死伤？这两个问题一直争论到今天，至今未有确切的答案。我对“二二八事件”的认识一开始之所以有偏差，也就在于我被他们的问题所吸引，忘记了追求真相的同时，正在展示人类历史上最大的真理。<br />
<br />
按照绿营和部分台湾居民的说法，这次事件造成的死伤人数高达20万，而国民党时期公布的资料加起来只有2千多人（本省人死亡673人），正是这种答案的反差，让我一开始感觉到民进党为了推翻国民党，把“二二八事件”当作悲情牌在打。心中颇不以为然。<br />
<br />
第二个让我失去了探求事件本身的意义的问题是：到底是谁下令开枪的？当时负责管理台湾的是陈仪，此人虽然是蒋介石的同学兼好友，却在“二二八事件”上知情不报，把蒋介石蒙在鼓里，最后收拾不住了，才请求蒋介石调兵进入台湾（当时蒋介石把军队都集中在大陆对付共产党）。<br />
<br />
民进党一直以来都指责是蒋介石下令开枪的，国民党却否认这一点，倾向是陈仪下令开枪，最后搞不定了才请示蒋介石，蒋介石本来是主张“怀柔”的。国民党指责民进党故意抹黑蒋介石，就是为了搞垮国民党。而民进党则指责国民党，维护最大的独裁者，其实是为了维护那个独裁者生存的土壤――一党独大的制度。<br />
<br />
就是这两个问题，让我相当长一段时间内沉溺在对“真相”的追求中，而在国民党和民进党的口水战中，我一度绝望地认为，真相不可能有大白的一天，受难者的数字不可能精确到各方都认同，签署屠杀的命令也不会从尘封的档案中跳出来。――这当然是以前的我，而现在的我却清楚的知道，我错得那么不靠谱。<br />
<br />
不错，当时民进党也许故意夸大了“二二八”事件中死伤的群众数量，甚至我们今后也不会得到确切的数字，可这并不是表明真相和真理被掩藏了。一个一党独大的政府，使用真枪实弹对付手无寸铁的游行示威群众，哪怕倒在血泊中的只有一个、两个人，他们的责任也并不比屠杀更多人要轻。<br />
<br />
另外，即便蒋介石当时完全不知情，下令对群众开枪的只是无能和失职的台湾行政长官陈仪，但作为最高领导人的蒋介石都难辞其咎――更重要的是，台湾民众和反对党并没有要求清算当时的独裁蒋介石，但却从来没有停止讨伐当时的那个独裁专制制度――那个把蒋介石扶持成最高领导人并授予他对民众生杀予夺大权的制度。<br />
<br />
“二二八事件”发生至今已经有62年了，真正的受害者（家属）、也是最有权力要求清算的当事人得到了相应的补偿，获得了平反，但这件事不但没有因此而销声匿迹，反而被台湾民众和政府不断地“发扬光大”。如果说民进党当初拿“二二八”事件攻击国民党政府是政党利益考量，如果说受害者（家属）要求一个说法是为了对死难者在天之灵的慰籍，而当国民党重新执掌政权后，不但没有放松对“二二八事件”的纪念，而且搞得更加隆重，更突出地彰显了“二二八事件”的历史和现实的意义：这不再是中国历史上屡见不鲜的一起屠杀事件。<br />
<br />
历史事件的意义也许并不完全在于这起事件本身，例如，中国历史上统治者屠杀群众的事件不说上万起，也绝对不少于一千，过去六十年就屡次发生。但那些历史事件最多给我们吃人的历史增加一个悲剧而已。<br />
<br />
历史事件的意义在于一个民族、一个“国家”对这件事的理解、共识和处理方式――“二二八事件”如果不被平反，不能让台湾人达成共识的话，它永远只能是一个悲剧。然而，我们看到，在全台湾的政党和民众深挖“二二八事件”的真相和意义的时候，在“二二八”这三个简单数字从当初的禁词和敏感词汇变得家喻户晓的时候，台湾民众已经把台湾历史上最大的悲剧演变成历史的分界线，树立为照亮历史也照亮未来的灯塔。“二二八”三个字也成为促成台湾和解、推动台湾在现代化道路上不停前进的恒久动力。<br />
<br />
而这一切是在没有报复，没有仇恨，没有清洗，只有和解与和谐的氛围中进行的。我相信，只要“二二八”这个数字不再次被禁止，也不被遗忘，那么，“二二八”这个数字将会成为约束统治者的魔诅，也成为民众用来保护自己正当权益的护身符。 <br />
<br />
台湾的民众和政党在坚忍不拔地追求真相的过程中，揭示了我们这个时代最伟大的真理――政府和人民的关系，这也是我们中华民族悠久了几千年而无法悟出的被现代文明证明为普适价值的简单道理。它既教育了台湾人，也教育了我这个一开始糊里糊涂的大陆人。<br />
<br />
现代化的历史证明，先进的国家比我们发达不是因为他们发明了汽车和飞机而率先冲进了未来，而是他们都比我们更快、更彻底地走出了历史的阴霾。不要掩盖历史，勇敢揭露真相，掩盖过去的民族，不可能有光明的未来，没有真相的国家，也与真理无缘！<br />
<br />
只有在真相的基础上才能达成和解，只有在和解的基础上才能真正实现和谐，这是“二二八事件”告诉我们的最大“真相”。是的，也许有那么一天，我们可以原谅，但直到永远，我们绝不应该遗忘……<br />
