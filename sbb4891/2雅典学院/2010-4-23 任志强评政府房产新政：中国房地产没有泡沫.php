													<h2>任志强评政府房产新政：中国房地产没有泡沫</h2>
						
						
																											<div id="postmessage_578831" class="t_msgfont">核心提示：北京华远集团总裁任志强于4月21日晚针对房产新政发表了长达一万三千字的博文。他认为,中国的房地产没有泡沫,遏制部分城市的房价上涨过快也不是为了将房价打下去,用行政措施遏制的房价、遏制需求而产生的销售下降并非是市场的正常供求产生的信号作用。<br />
<br />
<br />
<br />
<br />
<img src="http://img1.cache.netease.com/catchpic/F/FA/FAE588DD8007966DA5B0F4D0C2096FA0.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
<br />
华远集团董事长 任志强<br />
<br />
<strong>中证网4月22日报道</strong>&nbsp;&nbsp;国务院国发201010号文件即《国务院关于坚决遏制部分城市房价过快上涨的通知》出台。时隔近一周,北京华远集团总裁任志强于4月21日晚针对房产新政发表了长达一万三千字的博文。任志强在这份“万言书”中称,目前的房产新政只是一个开始,真正的重拳还在后面。他认为,中国的房地产没有泡沫,遏制部分城市的房价上涨过快也不是为了将房价打下去,用行政措施遏制的房价、遏制需求而产生的销售下降并非是市场的正常供求产生的信号作用。如果这个问题没有得到真正解决,未来如果消费出现报复性反弹,被压制的需求再一次释放时,或面临着供求严重失衡的局面。<br />
<br />
任志强在这篇题为《这只是个开始――我对国发201010号文件的解读》博文中,任志强称近期出台的房产新政为“有病乱投医,治标不治本”。他认为,市场只要用货币与利率的调节加上供求关系的平衡就能平稳解决的问题,而结果却是为控制资产价格的单一目标而可能失去了大局,至少失去了资源配置合理化的适宜信号。<br />
<br />
<strong>坚持市场调控与加大政府保障缺一不可</strong><br />
<br />
任志强在其博文的开篇便点明,国务院10号文件仅仅是个对各部委和地方政府下列计划任务的工作布置,而这些由各部委和地方政府出台的具体措施才是市场中必须执行和遵守的文件,文件的出台是个较长的政策决定过程,市场也一定会在这一系列的政策出台之前采取观望的态度。目前的市场反映最多也就是一种心理的压力造成的刺激,真正的重拳也许还在后面,尚有许多的未确定因素。<br />
<br />
任志强在其博文中多次担忧此次房产新政会成为市场经济向计划经济倒退的开始,他表示并不反对政府对市场的调控,但更希望政府用市场化的手段调控,而不是坚持用非理性、非制度化、非法律化的行政干预手段,这种违背市场规律的做法可以满足短期的政策目标要求,但却无法解决可持续的发展问题。<br />
<br />
在任志强看来,住房是民生的基础正在于政府必须严格的履行自己保障公民居住权利的责任,而绝不是用市场中的低房价来保障民生,而市场中的房价与民生没有任何关系。用稳定市场中的房价去替代政府保障民生的责任无异于将民生推向火坑,让更多本应由政府保障或帮助的群体备受煎熬,因为他们可能永远也没有能力进入市场。<br />
<br />
在1998年23号文件出台之后任志强第一个明确的提出在建立住房市场化的同时应建立强大的住房社会保障体系的。并且是第一个明确提出经济适用住房市场化中的弊病。此次在博文中,任志强又提出应早加大保障性住房建设,在加强该项制度时,首先要解决的是明确保障的对象和明确保障的方法。而不是满足大量借用保障性住房政策的名义而实现收入再分配的利益集团的欲望。尤其是应用立法的方式强制纳入各级人大监督的预算,将土地收益的合理比例部分投入于保障性住房的建设中。发达的城市尽管房价已高,但并不缺少建设保障性住房的资金,中央的补贴资金应明确的向非发达城市转移,恰恰不是向房价高的地区转移。而保障性住房的问责也恰恰不是上下级的关系,而应该是社会公开监督的、人大代表问责的关系。<br />
<br />
<strong>房价上涨根源在土地供应不足</strong><br />
<br />
任志强在博文中分析新政是否能够增加土地的有效供应时认为,房地产市场中的房价上涨,很明显是供给严重不足而产生的。他说,土地供给的短缺并非是今天才发现的事情,去年上半年投资的低增长和新开工的负增长以及全年的土地供给负增长,都早就预告了今年上半年会出现土地短缺供给中的天价,也早就预告了今年上半年住房供给在部分城市中的稀缺了。<br />
<br />
2009年房地产从冷到热的原因主要是4万亿投资、低利率、9.6万亿信贷和各种优惠政策,但销售回升时,投资与新开工并没有同步回升,任志强认为,这必然导致今年上半年的短缺。<br />
<br />
10号文件已经开始正面的回应土地的供给短缺的原因。文件要求增加居住用地的供应总量,但在任志强看来,这个总量的增加是增加在商品房的供给上还是增加在普通住房的建设上是两个完全不同的问题。他认为如果不将市场中的商品房供给明确形成增量的土地供给,就不会又房价的降低。<br />
<br />
政策中强调了改善土地招拍挂制度的方法,任志强则认为,如果没有土地的有效增量供给,不增加商品房的土地供给,仅靠挤压现有土地“增加住房的有效供给”并不能解决市场中的问题。不管供地的方式如何改变,不管供地的价格是否公开,或者供给价格高低,不解决总量都无法改变供求关系,也都无法解决市场价格的上涨问题,低价土地也一样会卖高房价。<br />
<br />
任志强同时担忧在各种政策的行政干预和打击之下,销售下降和房价的下跌将打击开发商的积极性,对房地产的投资和生产随着房价的降低而被遏制。<br />
<br />
任志强称,如果增加土地供给的时间提前半年或更早些就没有天价地和房价上涨过快的烦恼。他同时提醒,现在增加土地供应还为时不晚,但如果想增加供给的意图是在严厉的用各种行政、信贷、政治意识形态极浓的手段进行市场消费打压的情况下增加供给,就未必有效了。</div>

							
							
							
							
