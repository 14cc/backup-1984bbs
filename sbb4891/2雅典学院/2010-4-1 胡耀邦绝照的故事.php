													<h2>胡耀邦绝照的故事</h2>
						
						
																											<div id="postmessage_543458" class="t_msgfont"><a href="http://blog.sina.com.cn/s/blog_4c1c19620100h8ul.html" target="_blank">http://blog.sina.com.cn/s/blog_4c1c19620100h8ul.html</a><br />
<br />
张放 孟醒<br />
<br />
 <br />
<br />
 <br />
<br />
清明节即将到了，想起毛泽东摄影师老杜讲过的给胡耀邦拍照的故事来。那是1989年，也是清明节前后。一晃已经21年过去了。<br />
<br />
 <br />
<br />
那一年的4月初，中南海围墙外，胡耀邦的家紧贴中南海东门，但院门仍通着外面的胡同。担任党总书记后，胡耀邦并没有搬进中南海。为了工作方便，他就在院子后面围墙上，开了道通向中南海的门，前门一如既往开向普通百姓居住的窄小胡同。就是在这样一处既普通又奇特的院落里，胡耀邦度过了他的最后岁月。<br />
<br />
 <br />
<br />
杜修贤从上世纪60年代开始，担任周恩来的专职摄影师，70年代又担任了毛泽东的摄影师，不少中央领导人，都出现在他的镜头中。那时的胡耀邦，还是团中央书记，所以很少有机会捕捉到他个人的神姿风韵。虽然同在中南海里工作，却没有接触胡耀邦的机会。<br />
<br />
 <br />
<br />
1989年，胡耀邦离开了总书记的岗位，这时也不再担任中国图片社副总经理头衔的老杜，也正式离职归家。他要整理上万张黑白的或彩色的底片。他想把它们结集出版。那么，让谁为摄影集题名呢？老杜想到了胡耀邦，他曾经的总书记身份倒在其次，关键是他的人格魅力，他的正直热情侠肝义胆。另外，还有一层原因，那就是许多人对他的下台抱有同情。<br />
<br />
 <br />
<br />
4月，春暖花开。清明节后第二天，老杜请一位朋友引荐，来到了胡耀邦的家。<br />
<br />
 <br />
<br />
“老杜，我认识你，你是中南海里有名的老摄影记者嘛！”<br />
<br />
 <br />
<br />
老杜还没站定，手已被胡耀邦热情地握住。他的手传递出一股热流，让人感到他和你是平等的朋友。之前，很多人都评价说胡耀邦是个爽快人，此言非虚。所以，老杜也就直奔主题，说出了此行的目的：请他为摄影集题写书名。胡耀邦乐呵呵地把他让进客厅，老杜随即将带来的30多张放大24英寸的领袖照片，依次铺在客厅的地毯上。有毛泽东、周恩来、刘少奇、朱德、邓小平等党和国家领导人，以及陈毅、徐向前、聂荣臻、叶剑英等老帅们。<br />
<br />
 <br />
<br />
胡耀邦一张张细细观看，看毕，直起腰，又一次握住老杜的手：“拍得好，传神！”<br />
<br />
胡耀邦兴奋地问：“为这样的摄影作品题写书名，没问题！”<br />
<br />
 <br />
<br />
老杜边收拾照片，边介绍影集的有关情况：“这些照片都是我从以前拍摄的作品中挑选出来的，其中有许多镜头，已经是不可复得的珍贵孤品。在筛选照片时，我就请几位还健在的老帅题了词。徐帅题词：‘忠实再现历史’；聂帅题词：‘历史的见证’。封面题字就留给你。”<br />
<br />
 <br />
<br />
胡耀邦在延安时期，就以敢于直言出名，对延安整风运动提出不同意见，锄奸部就是在他的几次三番建议后，改名为保卫部的。但他被人们记住的，是改革开放之初，主持中央组织部工作期间，在邓小平的全力支持下，身体力行纠正大量冤假错案，使被打倒的一大批老干部重新走上政治舞台；将被批判的五十万“右派”摘掉反动帽子，使他们恢复了做人的尊严。他也被人们记住的，是后来以极大的勇气主持了“实践是检验真理唯一标准”的大讨论。<br />
<br />
 <br />
<br />
他自己承认在工作中有过错误，不过他勇于检讨、勇于承担，正因此，他的正直、坦诚、无私，使他赢得了更多人的尊敬。他的座右铭是：“公则明，廉则威，正人先正己。否则，上梁不正下梁歪! ”他为政清廉，一身正气，两袖清风，光明磊落。在他担任总书记不久，夫妻俩专门召开了家庭会议，胡耀邦严肃地指出：谁要是利用他的招牌和地位，在外面谋取私利，做了错事、坏事，谁自己负责。<br />
<br />
 <br />
<br />
 “那么，你老杜准备让我写什么呢？”<br />
<br />
“总书记您看看，写‘领袖瞬间’怎么样？”<br />
<br />
“你还是叫我胡耀邦，或老胡好。” 当时，胡耀邦已经不在总书记之位，只是个没有具体工作的政治局委员。<br />
<br />
 <br />
<br />
他还是那么平易亲切，热情如火，全然没有退位在家的失落。能保持这样良好的心态，实在难能可贵，很多人离职后，都会有一段时间不知如何是好，心里空荡荡的感觉。而像胡耀邦从全国瞩目的位置上退下来，并能这么快地从容应对，出人意料。<br />
<br />
 <br />
<br />
谈了一阵，抽了烟喝了茶，短暂的“热身运动”结束。胡耀邦起身，提议到他的书房。出了客厅，春天的气息已是扑面而来。书房在前院，再往前就是大门通向胡同的院子。书房虽然不大，但确是名副其实的书房，一溜书架占据住整整一面墙。胡耀邦在靠窗的宽大书桌前坐下，铺开宣纸，慢慢捻笔添墨，平息静气寻找下笔的感觉。他在砚台边轻捋笔毫，在宣纸上稍事停顿，一行墨迹翩然而出。<br />
<br />
 <br />
<br />
胡耀邦侧头看了看，不言不语地揭起放到一边，又写了一张。看看，又不满意，再写，一连写了六七张！他左挑右选，从中选出两张比较满意的交给了老杜：“我这是第一次给人写影集的书名，写得不好，见笑了。”他谦和地笑道。<br />
<br />
 <br />
<br />
走出书房，春光早已洒满庭院。胡耀邦的庭院相当窄小，不过二三十平方米，比农村的一间房子还小，是北京极普通的四合院。<br />
<br />
 <br />
<br />
老杜迅速为胡耀邦拍了张半身照，胡耀邦意识到在给自己拍照，就理了理身上的中山装，点头笑了。于是，老杜一连拍了好几张，才放下相机。<br />
<br />
 <br />
<br />
这时，胡耀邦出人意外地地提出要换身西装照一张。这在当时来说，很不同反响。那时候，中山装与西装几乎是“保守”与“开放”的代名词。国家领导人公开发表的照片，大都是穿中山装。穿西装，也许会出现不同的效果。果然，一身西装的胡耀邦，从屋里出来，让人眼前一亮。<br />
<br />
 <br />
<br />
老杜忙不迭地调好焦距和光圈，拍下了胡耀邦饱经沧桑、深沉凝重的一瞬。但老杜不知道，这张照片，居然成了胡耀邦的绝照！这最后的照片里，似乎能看到胡耀邦的脸上有种痛苦与忧虑感，但整个精神状况还很乐观。3天后，也就是4月9日，在中南海怀仁堂召开的中共中央政治局会议上，胡耀邦突然心脏病发作。<br />
<br />
 <br />
<br />
入院后直到4月16日清晨，胡耀邦对卫士说：“今天是16号吧，我要多走几步。”卫士请示院方，医生不同意。胡耀邦坚持说，自己今天精神挺好，问题不大。医生见劝不住，只好让他轻微活动。7时30分，胡耀邦告别人世。<br />
<br />
 <br />
<br />
得知胡耀邦4月9日发病的消息后，老杜马上叫车回家，拿出那天拍照的相机，第一时间送图片社冲洗出了给胡耀邦拍照的照片。16日，不幸消息传出。老杜连夜将胡耀邦中山装和西装照片各放大到24英寸，于4月17日上午送到了胡耀邦家。胡宅承受着巨大的不幸，也承接着平民百姓深切的爱戴和哀思。工人、农民、军人和学生怀着悲痛的心情，自发自愿络绎不绝地来到胡家，在前院的灵堂前，对着胡耀邦的遗像鞠躬致哀，并献上一朵白花。为民众作了好事的人，民众就会真心纪念他。<br />
<br />
 <br />
<br />
胡耀邦夫人李昭，颤抖着双手接过胡耀邦的遗照。她望着胡耀邦那张身穿西装的照片，禁不住流下两行清泪，她泣不成声地说：“……这张最像他……如果可能，我们家属希望用这一张做胡耀邦的遗像……”<br />
<br />
 <br />
<br />
经中共中央研究，同意了李昭和亲属们的意见。4月22日，中央主持的追悼会上悬挂的就是这张照片。这也是国家领导人第一次使用彩色照片作为遗像，第一次室外拍摄的自然照，第一次穿西装的遗像。也是国家领导人第一次使用没有笑容的照片作为遗像，总之，它突破了“标准遗像”的很多概念。<br />
