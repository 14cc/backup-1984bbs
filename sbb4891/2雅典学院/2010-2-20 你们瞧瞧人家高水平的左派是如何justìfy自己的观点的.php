<br />
 <br />
<br />
阿米绪（Amish）的故事<br />
<br />
<br />
　　<br />
　　◎丁林<br />
　　<br />
　　近几年随着中美文化交流的展开，美国一些远离主流的文化小溪，也渐渐被介绍到中国。于是，美国不再是一个刻板的固定套路，在大洋此岸人们的印象中，美国的形象正在逐步丰富起来。我曾经两次在国内的杂志上看到有人提到：美国有一群默默无声地生活在自己世界里的阿米绪人。<br />
　　<br />
　　假如我们对美国的一般印象可以放在概念的一个极地的话，那么阿米绪是肯定必须送到相反的另一个极地去的。如果我们称美国的生活方式是现代的，那么阿米绪可以说是古代的；如果我们称美国是技术进步的，那么在同一个价值体系里，阿米绪不仅是落后的，而且是拒绝进步的，等等。假如再形象化一些，如果我们对美国人的印象是眼花缭乱五彩缤纷的，那么阿米绪人永远是平淡的，是只有黑白的单色调的。<br />
　　<br />
　　假如在你的想象中，阿米绪是一小群生活在某一个群山环抱，车船不达，鸟不下蛋，与世隔绝的山洼洼里的话，倒也没什么稀罕了。问题是，今天的美国这样一群教徒有差不多35万人，人数还在缓缓地增加。他们生活在美国传统农耕区富庶辽阔的平原上。他们不但不封闭，甚至不集聚而居，没有什么阿米绪村庄，他们全是散户。一个个阿米绪农户就星星散散地座落在其他美国人的住房之间，混居在同一个地区。他们家门口的乡间公路也都是平展的柏油马路，直达高速公路。城镇就在附近，那儿就有购物中心和娱乐设施。嘈杂，多变而生气勃勃的现代生活就近在咫尺之遥。但是当你经过那里的民房，很容易辨别出阿米绪的住宅，除了都有巨大的谷仓之外，还在后院停着黑色的小马车。因为在我们断定不开汽车就算不得美国人的年代里，他们却只驾马车。<br />
　　<br />
　　所以，这不能不让认定”技术进步”必是”挡不住的诱惑”的人们愣一愣神。不管将来如何，你不得不承认，毕竟在世界上此类诱惑最大的美国，阿米绪已经两百多年这样默默地过来了，宁静安祥。也许不见得特别幸福，至少并不格外痛苦。他们也有自己的喜怒哀乐，但是并没有如我们想象的那样，被”诱”得骚动不安，六神无主，跃跃欲试，痛苦不堪。<br />
　　<br />
　　说起他们的来龙去脉，还得上溯到五百年前的欧洲。<br />
　　<br />
　　在十六世纪欧洲宗教改革的大潮中，从苏黎世产生出一个人数不多的激进改革派，被称为”再洗礼派”。他们主张严格实践圣经教义，排斥不符合圣经的虚文褥节。他们认为宗教信仰应该在日常生活中时刻加以实践，不能说一套做一套。他们认真地寻求圣经中对于大小事情的说法，弄清楚了就一定要去做，而且要做到。他们认为，教会应该是信仰相同的成人的集体。所以，婴儿出生以后”被动的”第一次洗礼不能算数。而在一个人成年之后，如果他确信自己真有信仰的话，应该”主动”地再接受一次基督徒的洗礼。这就是”再洗礼派”这一名称的来历。<br />
　　<br />
　　十六世纪还远不是一个宗教宽容的年代。再洗礼派一问世，就遭到来自罗马天主教会和其他新教徒两个方向的迫害。在再洗礼派发源的瑞士和德国南部，当时曾有几百个再洗礼派教徒被烧死在火刑架上。在这样残酷的环境中，再洗礼派却显示出惊人的宗教执着。他们认为，虽然他们面对的世界是傲慢的，富有的，偏狭的，暴戾的，而他们却仍然应该是善良的，清贫的，谦卑的，非暴力反抗的。在严酷镇压下，再洗礼派逐步形成了一些与其他新教教派不同的特点。他们无法形成良好的教会组织，一开始甚至只能在山洞里悄悄地聚会祷告。他们甚至没有明确的领袖，因为领袖一出来就给杀了。他们的一切都只能悄悄地做，恐惧，不安和受苦受难始终伴随着他们。<br />
　　<br />
　　既然没有严密有形的教会组织，没有教会规范的约束，也没有一般宗教常见的仪式仪规的凝聚，那么，他们作为一个教徒存在，就完全是依靠他们内心的信仰了。因此，假如说再洗礼派的信仰特别执着，大概是不错的。他们在北欧传播的过程中出现了两个支派。十六世纪中叶，一个叫做梅诺的荷兰人曾试图在北欧重建和平的再洗礼派的团体。他们的后继者就叫做梅诺纳特人，也就是梅诺派。而到了十七世纪末，瑞士和南莱茵河的再洗礼派还是处于遭受迫害的分散状态，有一个叫阿曼的瑞士人站出来号召再洗礼派的改革和联合，这一派就被叫做阿米绪，也就是阿曼派。<br />
　　<br />
　　这两个分支此后都来到北美这块新大陆，分布在美国22个州以及加拿大安大略省，大部分定居在俄亥俄州和宾夕法尼亚州。这两个州当年是北美大陆最好的农业区，而他们在欧洲时就是最出色的农夫。感谢开拓宾夕法尼亚的教友派，他们是对待异教最为宽容的北美主流教派，更要感谢北美大陆很快风行，并在美国独立以后由美国宪法保障的宗教自由，使这些”梅诺人”和”阿米绪”们，终于能够安居乐业了。由于阿米绪与梅诺人相比，他们更恪守古老的服饰和生活方式，在外观上更容易辨认，他们的风格与现代生活的反差也更为鲜明强烈，所以很多美国人也是只知阿米绪而不知梅诺人。<br />
　　<br />
　　宾夕法尼亚的兰开斯特县，是阿米绪比较集中的地区，离著名的大都市费城仅一小时车程。在去冬最寒冷的日子里，我们来到那里，也是想一睹真正的阿米绪生活。可是我们发现，他们安静谦卑地生活在自己的世界里。只要是对外开放，以满足游客好奇心而设立的”阿米绪旅游点”，那就一定不是阿米绪办的。因为这样获利甚丰的”第三产业”，并不符合他们的生活原则。然而，他们虽然在宗教上固守而且生活方式内向，可是他们对外界很友好，也理解外人对他们的好奇。假如你想给他们的小马车拍张照，他们会微笑着放慢车速，让你如愿以偿。我们发现，他们没有这类小教派非常容易出现的诡秘行迹，他们坚守的只是一种由宗教信仰导致的平淡。<br />
　　<br />
　　生活在宾夕法尼亚的再洗礼教徒至今没有显赫的教堂，他们的教堂一如他们的农舍，朴素而卑微，有时候甚至象早期教友会一样，把他们聚会崇拜上帝的教堂谦称为会屋。老派的阿米绪还轮流聚集在各自的农舍里做礼拜，围着老式的乡村火炉，一边坐着男人，一边坐着女人，读着老版本的圣经，用的是这里其他人都不懂的高地日尔曼语。而在他们只有一间房间的学校里，他们的孩子则个个都要学两种语言，英语和这种他们从欧洲家乡带来的古老语言。<br />
　　<br />
　　无论在什么地方，你一眼就能把阿米绪认出来，因为他们的服饰与众不同。简单地说，其他的人，四五百年来的服饰一直在变，而他们却一直没有变。不论是男人的服装礼帽，还是妇女的衣裙，都是一水的黑色。只有在节日或婚礼上，妇女们才加上一方纯白的披肩。姑娘们的裙衫上没有一个钮扣，男人们的服饰上有钮扣，但没有任何其它装饰。据说这些突出”谦卑”的规矩都可以从圣经中找到依据。在兰开斯特，假如你遇到以前只在电影里才能看到的”古代欧洲村民”，他们就是阿米绪。<br />
　　<br />
　　也许，他们的上空就是高压线，他们的邻居就是家用电器样样俱全，但是他们不用电。所以，也没有电灯，电视，电冰箱，收音机和微波炉。阿米绪不用汽车，他们是农夫，却拒绝使拖拉机和任何新式机械，有些梅诺人偶然使用汽车，但一定是黑色的，以示谦卑。阿米绪则用马拉犁耕地，驾着单驾马车外出。我们看到，在兰开斯特的乡间公路上，答答疾驶的阿米绪马车后面，常常跟着几辆耐心的邻居们的汽车。<br />
　　<br />
　　对于认定只有自己的价值体系是唯一正确的人们，很难理解为什么阿米绪放着现成的新技术拒不使用。他们除了认定阿米绪固守落后，再也找不出别的解释。然而在多元文化的概念逐步被人们接受的今天，人们能够看到，这些再洗礼派教徒非常聪敏智慧，也非常能干。他们在兰开斯特县用传统农业技术经营的家庭小农庄，是全美单位出产最高的农庄之一，而且没有化学污染土壤退化等现代农业的通病。他们的生活简单而安逸。他们自己的解释是，由于他们的宗教信仰和几百年来所遭受的迫害，他们对整个外部世界抱有深刻的戒心，他们强烈地要和外部世界的浮躁轻薄和人性渐失，保持一个距离，从而能不受诱惑干扰地追随他们的上帝。他们认为眩目的电器是对他们的精神世界的威胁。<br />
　　<br />
　　兰开斯特公路上汽车马车混列行驶的景象在美国实属罕见，因此，在离开之后我们依然久久难以忘怀。这也引起了我们的另一个好奇：阿米绪既然是混居在普通的美国人中间，他们要解决的问题显然不止是汽车和马车的矛盾，他们独特的宗教信仰与几百年前的生活方式，同现代化的外部世界是如何协调的呢？这个问题最突出的部分，就是他们和各级政府依据现代美国生活所制定的法律冲突是如何解决的的呢？<br />
　　<br />
　　之所以我们会立即产生这样的联想，就是因为在美国生活了几年之后，我们知道这是一个重法律契约并且执法很严的国家。在一个民主社会，法律和政府的决策是多数达成一致的结果，所以法律一旦通过，就要求个人服从，包括持有不同意见的少数，而阿米绪显然是美国的少数。<br />
　　<br />
　　在两个不同文化发生冲突的时候，最重要的往往是妥协的精神。阿米绪是一个谦卑的群体，因此，他们在自己能够接受的范围内，也在作最大的妥协。兰开斯特县议会曾经立法规定，阿米绪的马车在公路上行驶时，车后部必须安装一个橘黄色三角形的慢行车标志。尽管这有违阿米绪不行装饰的传统，但是他们理解交通安全的合理性，如今他们的马车上都有这个标志，衬在黑色的车蓬上，格外醒目。同时县议会立法规定，马车夜间行驶不能使用老式黯淡的油灯，而必须使用干电池的车灯。阿米绪是不用电的，但是，这一次他们还是接受了这个干电池车灯。又如，兰开斯特县为了公共卫生，立法禁止没有化粪池的户外厕所。阿米绪于是改变自己的习惯，把厕所移到室内，并且都接受了地下化粪罐这样的”新技术”。<br />
　　<br />
　　然而，在现代美国社会中，还是有一些与阿米绪宗教信仰完全冲突的法律，是他们无法妥协的。这种冲突有时还相当尖锐。这就是考验这个国家的时候了。因为民主意味着按照多数人的意志行事，但是如何对待少数人，始终是民主社会一个难解的课题。美国也经历了一个逐步认识的历史过程。<br />
　　<br />
　　首先遇到的就是一个大问题：战争与和平。当美国卷入两次世界大战，以及以后的朝鲜战争，越南战争时，国家都需要有人当兵打仗。征兵法应该对所有的公民一律平等，这是现代国家的一个常识。但是阿米绪所属的再洗礼派是绝对的和平主义者，他们的信仰使他们坚守不动刀兵。于是，他们和美国的法律开始了旷日持久的冲突。<br />
　　<br />
　　那么，绝对的和平主义者究竟意味着什么呢？在阿米绪代代相传的一本书中，记述了他们人人熟知的一个历史故事。早年间，有一个荷兰再洗礼派教徒叫迪尔克。他由于遭受宗教迫害被警官追捕，追捕过程中警官掉进了一条冰河。迪尔克明知自己一旦被捕将会性命不保，他还是不能见死不救。他返过身来救起警官，自己却因此被捕，被烧死在火刑架上。这件事发生在1569年，在他们来到北美大陆以前。<br />
　　<br />
　　在他们来到北美大陆以后，也有类似的事情发生。在白人和印地安人互相追杀的年代，有一次，一伙印地安人在夜间包围了一个叫雅各布的阿米绪家庭。雅各布的儿子们本能地操起了打猎用的猎枪准备自卫。雅各布却夺下他们的猎枪扔了出去。结果是，他们一家除了两人被掳走以外，全部被杀。这并不是一个孤立的例子。不仅是阿米绪，其实当时还有大量教友派教徒也由于坚持和平主义而被印地安人杀死。以上这些历史都是阿米绪教育后代的典范。<br />
　　<br />
　　在一次大战期间，一些阿米绪的年轻人被迫入伍，参与军训，甚至被迫拿起了枪来。然而，不管是什么理由的战争，对于再洗礼教徒都是不可接受的。所以阿米绪抗拒军令者甚众。那是近一百年前，美国政府战争当前，显然不打算在这个时候与阿米绪探究什么宗教哲学问题。因此，在这次战争中，有很多阿米绪教徒因拒服兵役而被逮捕入狱，也有人因为在阿米绪的报纸上告诫教徒遵从教义反对杀戡而被判”煽动不服从罪”。<br />
　　<br />
　　当时，有一个叫鲁迪的阿米绪被征入伍。军官逼着他穿上了军装，列队操练。几个星期以后，轮到实弹演练的时候，他再也受不了良心的谴责，脱下军装，要求退伍。按当时的法律，违抗军令要受军事法庭的审判。军官把他带到军营外的三座新坟边，拍着手枪警告他，明天早晨如若不穿上军装报到，他就将是第四座坟墓。鲁迪一夜无眠。第二天早晨，新兵们吃早饭的时候，鲁迪来了。他穿着一身黑色的阿米绪传统服饰，戴着黑色的帽子。为了宗教良心，鲁迪作了死的选择。从来就说一不二的军官看着这个阿米绪，却没有枪毙他，让他退伍了。<br />
　　<br />
　　在一次大战期间，这个问题每个个案遇到的情况不同，处理的方式也不同。冲突发生了，问题却没有解决。由于阿米绪人数不多，牵涉的面不广，没有引起太大的注意。<br />
　　<br />
　　到了二次世界大战，德国是主要敌对国。恰巧很多阿米绪祖先的故乡就是德国。他们在教育中很重视保持故乡语言，阿米绪的日常语言往往是高地日耳曼语。所以，这一次他们的反战行为在美国引起了关注。有人怀疑他们拒绝作战，是因为他们站在自己的母国一边。美国国会为此专门举行了听证会。为了阐明再洗礼派的和平主义宗旨，争取不上战场的权利，一向不愿抛头露面的阿米绪派出代表在国会听证会上宣誓作证。<br />
　　<br />
　　他们以自己的历史向全美国人民证明，他们反战只是出于他们的宗教信仰。他们反对的是任何一种战争，不论其交战国是谁，也不论其战争原因如何。<br />
　　<br />
　　在他们绝对和平主义的宗教立场被确认之后，多数向少数作出了让步。在二次大战这样的险恶环境中，美国国会依然承认一个事实，人和人不一样，少数人有少数人的理由。美国军方为称作”良心反战者”的阿米绪作出了特殊安排，称为”替代性服役”。他们必须在军方安排的医院或工厂从事两年没有酬劳的，与战斗没有关系的工作，以代替服兵役打仗的公民义务。<br />
　　<br />
　　阿米绪历来只在自家农庄上务农，很少外出就业。他们认为，这样的两年”替代性服役”仍然使他们被迫融入外部生活，并把外界躁动的气息带进了再洗礼派虔诚平静的生活。再洗礼派再次向国会申诉。经过长期的努力，现在的”替代性服役”改为”自愿性服役”，阿米绪可以在自己的教会管理的农庄上从事两年没有报酬的农业工作，以代替服兵役。<br />
　　<br />
　　美国国会作出这样的决定并不容易。二次大战对所有的参战国来说，都是一场惨烈的厮杀。在战争接近尾声之前，谁也说不上胜负的必然走向。在这种非常状态下，作为一个国家的”多数”，同意用自己的血肉之躯去为一些声称是”和平主义者”的”少数”抵挡敌人的子弹，其原因仅仅是因为尊重这些”少数”的宗教信仰，假如没有理性的精神，几乎是做不到的。<br />
　　<br />
　　美国在历史上屡有无视少数的过去，成为他们迄今为止不断反省的原因。现在你如果在美国游览的话，常可以遇到一些历史纪念牌，记载了插牌所在地发生的一段历史。有不少这样的牌子在检讨当年对待印地安人和黑人的不公正。正是这样的反省使得美国在对待少数的问题上，变得越来越谨慎，也越来越宽容。<br />
　　<br />
　　这个国家的多数和阿米绪是有冲突的，但是双方都以理性为基础，尤其在处于多数优势的主流社会一方，逐步地在学会如何尊重少数，以达成妥协。因此，阿米绪虽然是美国公民，但是他们的公民义务和权利与一般美国人是不完全一样的。例如，美国的税收很高，这样的税收虽然不在阿米绪传统的自给自足生活方式之内，阿米绪还是依法纳税。可是另一方面，他们也和政府达成协议，他们以传统方式颐养天年，从不出现老无所养的问题，他们不享受美国的老年福利金，也就不交纳税收中用于社会养老的社会安全基金。他们也不承担美国公民的一项重要义务，就是他们不担任法庭的陪审员。因为在他们的宗教信仰里，只有上帝有权判定人们的罪孽或清白。<br />
　　<br />
　　在历史上，美国法律与阿米绪的一次最大的冲突，是在教育领域发生的。这场冲突，很典型地反映了”少数”与”多数”在文化上的差异可以有多大。<br />
　　<br />
　　阿米绪的传统学校是所谓”单室”学校，顾名思义，学校只有一间房间。它也只有一个老师，所有的孩子都在一起上课，为的是让他们学会互相帮助。美国人一贯认为，选择怎样教育子女，这是父母的权利，美国历史上延续至今一直有家庭学校的做法。问题是，阿米绪还认为，孩子读书到十四岁，相当于八年级，就够了，从十五岁起就应该到农田里干活了。他们认为外面的孩子从十五岁开始的高中教育对阿米绪是有害无益的。可是，教育立法和他们的教育方式直接冲突。<br />
　　<br />
　　美国的教育管理权归属各州，对中小学最有发言权的是各地的学校理事会，有家长和教育界人士共同组成。各州的议会有教育立法权。为了维持整个社会全体民众的教育文化水平，各州议会在上世纪末就先后立法实行强制性的义务教育。各州的普及教育立法是顺应时代潮流，立足于提高全民文化水平，深得民众的支持。至今为止，我还很少听到有其他人反对义务教育法规的。当阿米绪所定居的那些州开始立法规定强制教育至十六岁时，阿米绪教徒教育自己孩子上学只到十四岁的做法违法了。<br />
　　<br />
　　阿米绪当然也理解，州政府在教育上的强制立法，并没有恶意。但是他们认为，公立学校的教育方式，会引导他们的孩子脱离他们代代相传的宗教追求，是对他们的宗教传统的威胁。这不是没有道理的，专家们曾经指出，再洗礼派的教育，在维护传授价值观念方面起了不可低估的巨大作用。对于他们来说，能否自己教育子女，等于自己能否生存延续。<br />
　　<br />
　　少数不可以藉着不同意而不服从法律，这是美国的游戏规则。唯一的合法途径是申诉，而少数人的合理申诉能够得到公正的对待，也是游戏规则能够操作下去的前提之一。申诉有两种方式，一是向行政和立法分支和平请愿，二是向司法分支提出法律诉讼。阿米绪的神父们劝告大家不要和政府冲突，也不要上法庭去打官司，因为这违背了阿米绪教徒和平主义与世无争的传统。神父们决定向州立法与行政两大分支请愿，请求网开一面。<br />
　　<br />
　　宾夕法尼亚的阿米绪把请愿书印了一千份，然后征集签名。阿米绪教徒人数虽少，可是他们捍卫信仰的精神以及和平谦卑的态度却深得宾夕法尼亚人的同情。签名者甚众。往往一个阿米绪就可以征得三千个来自外部世界的签名。他们把这些签名联成130英尺长的条幅，然后，阿米绪的代表就带着它去见州长。州长于是下令州司法部长进行调查，新的教育法是不是侵犯了宗教自由。阿米绪因此很感激这位州长，在随之而来的感恩节，他们给州长送去一篮农家产品，包括一只火鸡，一罐糖浆，还有一些苞米。但是，调查结果并没有解决问题。<br />
　　<br />
　　阿米绪知道他们还可以走的一条路就是司法途径。但是，阿米绪不喜欢提起诉讼，他们只习惯于诉诸上帝。这一次，他们无路可走，假如不想放弃的话，唯一可行的是”以身试法”了。实际上这是一般美国人在自己的观点处于主流文化之外的时候，常常采取的方式。这一类以被动形式出现的司法挑战，常常引发对一个主流观念的质疑，甚至可能改变这样的观念。而”被动”，正是阿米绪的特点。<br />
　　<br />
　　阿米绪从不惹事生非，但他们不送自己的孩子读高中，警察就找上门来了。美国是一个执法很严的国家。在本世纪初，就有阿米绪家长因为违反义务教育法而被捕，他们的孩子则由政府监管。为了家庭的团聚，他们要么屈从，要么被迫变卖家产，举家迁徙，有些阿米绪家庭甚至为了逃避义务教育法，迁移到遥远的墨西哥去。他们不愿意在压力下改变他们的宗教信仰和与此相随的生活方式。<br />
　　<br />
　　宾夕法尼亚是一个有着宽容精神传统的州，我们去过的兰开斯特居住着全美第二大的阿米绪人群。州教育法通过后，一开始地方政府对如何向阿米绪执法也很困惑，所以出现了比较罕见的执法不严的情况。地方上对阿米绪少年缀学到地里干活，基本上取睁一眼闭一眼的态度。但是到了1937年，兰开斯特的教育官员觉得阿米绪传统的单室学校实在不够正规，并且没有高中教育。就计划关闭一些单室学校，以新建的公立学校作为替代。州政府的代表想说服阿米绪，”教育是通向知识的大门”。可是，这种对话建立在完全不同的文化价值体系里，自然是谁也说服不了谁。这样的文化冲突使州政府和阿米绪都深感不安和困扰。二次大战中，这个问题被搁置一边，战后又立即重提，为此又有一些阿米绪家长由于违反义务教育法而被课以罚款，甚至被捕坐牢。<br />
　　<br />
　　他们再次向州政府请愿。直到1955年，州政府作出妥协：阿米绪人可以在自己的学校里教育子女到14岁，然后为他们设立一种专门的职业学校，阿米绪的孩子在这种职业学校受教育到法定的16岁。这种职业学校一周只上半天课，并且是由阿米绪的教师讲授传统的农业知识。这个安排是一个突破，所以在美国历史上很有名，称作”兰开斯特职业学校妥协”。这里的阿米绪完全是靠着一种不可摧毁的宗教精神韧性，和强大的多数人的政府达成妥协，嬴得了按自己的意愿教育子女的权利。美国史书上的妥协一词通常是一个正面的词。大家认为，达成妥协解决了问题，是双方共同的胜利。<br />
　　<br />
　　由于教育立法权归属各州，所以”兰开斯特职业学校妥协”并没有解决其他州的类似冲突。问题是普遍的。在爱荷华州的布肯南县，地方政府宣布要取消单室学校，把阿米绪学童集中到新建的公立学校；而阿米绪却坚持把孩子送到他们的单室学校去。在1965年11月的一天早晨，县上的教育官员带着警察，开着校车，来到一所阿米绪的单室学校，要把孩子们押上校车送往新学校。单室学校只有一个阿米绪老师，无可奈何地看着孩子们列队出去。突然，不知是谁喊了一声。没等警察和官员回过神来，孩子们象炸了窝一样，拼命地冲向附近一望无际的苞米地，刹那间就消失在青纱帐里。原来是一个阿米绪孩子喊了声”快跑！”，他用的是阿米绪的高地日尔曼语，其他人谁也听不懂。<br />
　　<br />
　　随行的新闻记者凭着职业反应，迅速拍下了穿着黑色衣衫的大小男孩女孩，象兔子一样惊慌逃向田野的背影。这张照片后来非常有名，因为这个问题后来终于在全美国人民同情的目光下，走向了联邦最高法院。<br />
　　<br />
　　在有些地方的阿米绪学校，学童们被成功地押上校车转学，两种不同价值相遇形成十分荒诞的后果。一个为了提高孩子教育水平的法律，实行中却场景凄凉。阿米绪孩子们唱着”上帝爱我”，母亲们无声地哭泣，父亲们则青着脸，默默地站立一旁。阿米绪人依然奉行和平主义的原则。但正是这种沉默，谦和然而执着的态度，以及美国民众对于”多数与少数”关系的反省，使得阿米绪的教育事件开始走向全美国。来自全国各地的私人捐款涌向布肯南县，要求替阿米绪人偿付罚款。这种同情和抗议给爱荷华州州长带来很大的压力，但是他作为行政长官无权修改法律。他只能在他的职权范围内，宣布暂停执行合并学校三周，同时请求拥有立法权的州议会，考虑立法豁免阿米绪的强制教育。1967年，爱荷华州议会将豁免权授予州行政部门的教育官员，从而，爱荷华州的阿米绪也嬴得了以自己的方式教育子女的权利。<br />
　　<br />
　　就在这个时候，一个叫林德赫姆的人挺身而出。他不是阿米绪，而是路德教会的一个牧师。他了解了阿米绪在教育问题上的遭遇以后，认为阿米绪的宗教自由受到了侵犯。1967年3月，他在芝加哥大学一个有关公共教育立法的学术会议上，呼吁关心阿米绪宗教自由权利的人伸出援手。一个叫做”阿米绪宗教自由全国理事会”的组织就这样诞生了，林德赫姆担任了这个理事会的主席。这个组织不仅有律师，学者，还有基督教和犹太教的宗教领袖。<br />
　　<br />
　　这时全国关注的目光移到了堪萨斯州，那里有阿米绪的家长被捕，还有人在法庭上被定罪。堪萨斯州态度强硬地宣布，类似宾夕法尼亚州的”职业学校妥协”的做法，在堪萨斯州将是非法的。州一级不打算妥协。林德赫姆的组织曾经试图把案子上诉到联邦法院，但是联邦最高法院拒绝接案，其原因是美国”分权”的制度。在这个制度下，教育管理是留归各州的权力，联邦政府无权干涉。因此，联邦法庭也就没有这些案子的司法权。<br />
　　<br />
　　于是，那里的阿米绪又决定迁徙。不少人就这样迁到了威斯康辛州的格林县。但是到了1968年秋天，这儿也开始严格执行教育法规。又有两家阿米绪面临被捕，被指控的罪名就是没有送孩子上高中。1968年圣诞节前夜，林德赫姆和一个叫鲍尔的律师，在请求威斯康辛州政府豁免阿米绪遭到拒绝后，决定在格林县的法庭，代表阿米绪向州政府打官司。告州政府侵犯阿米绪的宗教自由。可是，官司输了。地方法庭认为，虽然可以说州政府侵犯了阿米绪的宗教自由，但是普及教育涉及全体公民的长远利益。这一利益压倒了少数人的宗教权利。<br />
　　<br />
　　这在美国是经常发生的事情，就是在两个法律条款发生冲突的时候，必须判断何者为先。出现这样的法律悖论的时候，一般总是要走到联邦最高法院，因为最高法院具有”司法复审权”。这正是鲍尔律师想要达到的目的。他不是打算在地方法院就打嬴这场官司，他甚至知道他会输。但是他要开辟一条司法渠道。鲍尔先上诉到威斯康辛州最高法院，州最高法院推翻了地方法院的判决，法官说，能够压倒少数人宗教自由权利的所谓全体人民的利益是不存在的，阿米绪选择八年教育并没有损害社会。<br />
　　<br />
　　于是案子的被告，威斯康辛州政府的行政分支，开始向联邦最高法院上诉。前一次，案件的性质是判定阿米绪孩子的教育管理问题，这个问题联邦法院没有司法权。可是现在，案件的性质是全国性的民间团体代表百姓控告州政府侵犯宗教自由，也就成了州教育法规是否违宪的问题，这属于联邦最高法院的审理范围。于是，这一次，联邦最高法院接受了这个叫做”威斯康辛诉约德尔等”的案子。鲍尔律师出庭辩论，一些从不抛头露面的阿米绪也默默来到首都华盛顿，听候决定他们命运的判决。他们还是一袭传统阿米绪的黑色服装。黑色的背影衬映在最高法院白色大理石建筑的背景上，使我们今天看到这张过时的新闻照片时，依然有惊心动魄的感觉。<br />
　　<br />
　　1972年年底的一天，最高法院大法官以压倒多数作出了有利于阿米绪的判决。首席大法官沃伦在判词中指出，现代中等教育所教授的内容和价值同阿米绪宗教生活的根本方式有尖锐的冲突，强制实行的教育法规侵犯了阿米绪教徒的宗教自由权利。<br />
　　<br />
　　在最高法院的判词中，沃伦大法官写下了如下这段现在还常常被人引用的话：<br />
　　<br />
　　”我们不可忘记，在中世纪，西方世界文明的很多重要价值是由那些在巨大困苦下远离世俗影响的宗教团体保存下来的。没有任何理由假设今天的多数就是‘正确’的而阿米绪和类似他们的人就是‘错误’的。一种与众不同甚至于异僻的生活方式如果没有干涉别人的权利或利益，就不能仅仅因为它不同于他人就遭受谴责。”<br />
　　<br />
　　最高法院的判决，一劳永逸地解决了各州与阿米绪在教育问题上的冲突，沃伦大法官的判词，更是对美国人民长久以来的思考和反省，作出了一个总结。民主制度要求少数服从多数，同时要求多数不能压迫少数，不能侵犯少数的自由和权利。要做到这一点，在制度的设计上，一开始就要为持不同意愿的少数预留下申诉，辩解和反抗的渠道。在百分九十九点九的一致之下，仍要为百分之零点一的异见留下呼吸的空间。这也是美国法律强调个人的宪法权利必须归属个人，而政府”不得立法”侵犯这种权利的根本原因。<br />
　　<br />
　　如果法律不打算保护千分之一万分之一，也就保护不了”百分之五”，那么，”多数”本身也就都潜在地岌岌可危。我们曾经习惯于法律对”百分之五”的不予保护，这是因为，当我们身处”多数”之中，我们理所当然地认为，”多数”就是对的，我们只知道庆幸自己不是少数。谁也没有想过，今天你不挺身而出保护你所不同意甚至不喜欢的百分之五，你怎么有把握下一次你不在另一个百分之五中呢？今天你看到与你无关的百分之五遭受的不公正扭过了头去，下一次轮到你的时候，你还向谁去呼喊呢？<br />
　　<br />
　　一个社会要发动成千上万的人并不难，要达到多数人的一致也不难，难的是公正善待只有百分之几的少数。有时候，少数显得如此人微言轻，他们的生死存亡是如此地微不足道，可是，一个制度能否保证这微乎其微的少数得到公平的善待，恰恰是检验这个社会是否文明和人道的试金石，也是决定这个制度能否长治久安的一个关键。<br />
　　<br />
　　也许，最平凡的阿米绪正默默地以他们的存在，在给人类讲述着一个并非无足轻重的故事。</div>

							
							
							
							
