																											<div id="postmessage_486440" class="t_msgfont">穷人的普世价值<br />
穷人的普世价值<br />
同人于野<br />
我听说有这么一种中学。在这个学校里，学生几乎没有任何自由。<br />
怎么走路，怎么坐，走路的时候怎么拿东西，怎么回答问题，甚至上厕所之后怎么洗手，都有严格规定。<br />
课堂上别的同学发言的时候，全班同学按规定动作看着他。在教室里，学生必须学会使用两种统一的音量说话，根据具体情况决定使用哪种音量。如果哪个同学在课堂上有小动作，老师会立即停止上课，然后全班讨论怎么“帮助”他克服这个坏毛病。<br />
这个学校的学生一般早上五六点钟就起床，在七点之前已经开始集体的学习。放学也比别的学校晚，一般要在下午六点以后，然后回家还要做两个小时的家庭作业。<br />
除了刻苦学习之外，学校还要求所有学生必须有礼貌 ― 不是一般的有礼貌，是日本艺妓水平的礼貌。比如你跟这个学校的一个学生说话，他会注视你，不停地点头表示非常赞同你的观点。老师甚至有意在校园里放置垃圾，让学生们自觉捡起来。<br />
学生们既不研究体育明星，也不常去博物馆，他们学习的核心只有一个，那就是一定要考上大学。<br />
有人研究表明，在这种学校里，老师一直向学生灌输的观念可以归纳为三条：<br />
第一，你是集体的一员。<br />
第二，外面的世界奇怪而危险，所以我们的集体要始终团结在一起。<br />
第三，世界就要像一座大山，山的顶峰是一个天堂，这个天堂就是大学。<br />
这三条如果不是说一个学校，而是一个国家的话，必然是一个民族主义到了军国主义程度的可怕国家。这种学校在湖南农村么？或者这种学校在二战前夜的日本么？<br />
这种学校在今天的美国。这就是美国的 KIPP (Knowledge Is Power Program) 学校系统。<br />
美国的公立学校系统总的来说相当差劲。如果让一个孩子在纽约的某些学区上中学，他受到的教育和未来能考上美国大学的可能性，也许低于中国某些大城市的中学。KIPP 就是公立学校改革的产物。在这个学校系统上学的绝大多数孩子来自穷人家庭。然而 KIPP 却获得了超过80%的大学升学率，其各项成绩往往是所在州所有公立学校里面最好的。这种学校是如此热门，它的到了大量的社会捐款，而谁能入学必须抽签决定！<br />
我国的自由主义知识分子会对这种学校有什么评论呢？这哪是培养人才，纯粹是培养机器。中国的主流舆论，早在20年前就开始鄙视“以考大学为核心”的中学教育，认为素质教育才是最重要的。我国的舆论认为，让孩子处处按规定动作行为，是对人性的摧残。过分的礼貌则无助于养成独立的人格。宽容，自由，平等，这才是普世价值。<br />
但从 KIPP 在美国的成功，我们可以看到另一种普世价值：穷人的普世价值。穷人的普世价值很简单，那就是不想再当穷人。哪怕是牺牲个性，也不想当穷人。哪怕是没时间看哈利波特，也不想当穷人。哪怕是集体主义，也不想当穷人。<br />
在某些人眼中的完美穷人，是安分守己的，以贫穷为乐的穷人。一个中产阶级人士跑到乡下去玩，他希望看到的可能是那种乐呵呵地赶着牛羊去田野的穷人孩子。在最理想的情况下，这个孩子最好还要对他的跑车表示一下鄙视，那样的话他会感动地把这个经历写在博客上。反过来，如果一个穷人孩子对他说，我要不惜一切代价考上你上的那个大学，将来也买一辆这样的跑车，那就一点诗情画意都没有了。<br />
西方国家的人看西藏和非洲，就是希望这两个地方的穷人永远都是诗情画意的穷人。他们希望藏族人最好永远都是虔诚的宗教教徒，他们希望非洲人最好永远都过绿色环保的生活。<br />
但穷人不想永远当穷人。穷人认为如果能考上大学，那么年轻的时候做出一点牺牲是值得的。考上大学以后再学风雅也不迟。即使是在今天的美国，仍然需要 KIPP 这样的中学，而且不是一般需要，是极其需要。美国媒体对 KIPP 是一片欢呼声。穷人没有别的社会资源可以改变自身命运，升学率就是硬道理，这么基本的常识有什么可说的？<br />
我认为穷人的普世价值，是比知识分子的普世价值更“基本”的价值。中国在三十年以前，主要由穷人组成。所以中国那时候唯一正确的做法，也许就是设计一个类似于 KIPP 学校的系统。抽象地谈论普世价值，指责当初的做法，有什么意义呢？</div>

							
							
							
							
