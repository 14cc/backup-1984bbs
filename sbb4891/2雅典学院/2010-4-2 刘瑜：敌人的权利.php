																											<div id="postmessage_545329" class="t_msgfont">谨以此文献给那些动不动拿自由民主国家一点失误来恶毒攻击这些国家、拿鸡毛当令箭的人士。<br />
用圣人的标准要求民主国家，是五毛党不自觉的选择；而不少向往自由民主的朋友，也往往陷入这种思维怪圈里头，当五毛党拿这些事情说事的时候，往往否定这些事情，一旦这些事情坐实，又往往会陷入失望。其实，大可不必。<br />
人无完人，孰能无过？过错不能否认，也否认不了，关键是，有了过错，看怎么承认、改正，关键看纠错机制。<br />
这篇文章最近读了，觉得不错，转贴过来。<br />
<br />
敌人的权利 <br />
<br />
刘瑜 <br />
<br />
<br />
以前听说过一句话：检验一个国家的文明程度，不是看多数人，而是看少数人的权利是否得到保护。要我说，还有一个更过硬的标准，就是看这个国家的“敌人”落到它的手里之后，权利有没有得到保护。 <br />
<br />
对目前的美国来说，敌人最集中的地方，莫过于关塔那摩监狱了。“9・11”以来，那里先后关押了700来个“恐怖分子嫌疑人”，这些人未经法庭审判，被长期关押。 <br />
<br />
06年3月3日，《时代》公布了02年底到03年初一个囚犯卡塔米的审讯记录，其中曝光了审讯过程中的种种“虐行”，包括：让他扮狗羞辱他、长时间审讯不让睡觉、用一个非常不舒服的姿势长时间拷住、降低房间温度并不断向他泼冷水、长时间放特别吵的音乐……据称，卡塔米的待遇在关塔那摩是个普遍现象。无独有偶，03年底04年初，伊拉克阿布・格莱布监狱美军虐待战俘照片曝光于各大媒体，举世轰动，可以说让美国的国际声誉沾上了难以洗刷的污点。 <br />
<br />
无论是阿布监狱的照片，还是关塔那摩的记录，都表明“敌人”落入美国手里之后，人权受到了严重侵害。但是不是就可以得出结论，说美国的人权概念根本经不起推敲，不过是一根用来敲打它国的大棒呢？如果得出这个结论，只能说观察者只关注了美军虐俘这个现象，却没有关注虐俘现象曝光后，美国社会和政界的反应。 <br />
<br />
我们都知道，对一辆长期在路上跑的车来说，遇上或大或小的交通险况，几乎是不可避免的。但是，危险是否最终会酿成悲剧，还要取决于车里的很多危机应对设置，比如，刹车是否灵敏，车内乘客是否系安全带，车内的充气口袋会否及时弹开等等。虐俘行为，可以说是美国这辆“自由号街车”遇到的险情，说明美国的人权状况还存在严重缺陷。但是，从美国社会各界的反应来看，这辆汽车的刹车、安全带、充气口袋又非常可靠，在汽车从“自由线路”滑向“野蛮线路”前来了个急刹车，及时避免了更大的危险。 <br />
<br />
媒体、民间社团的力量，可以说是刹车装置；立法系统的制约，可以说是安全带装置，独立的司法力量，则是充气装置。所有这些避险机制及时启动，最后结果是，虽然布什政府这个“司机”开错路线几乎翻车，最后还是有惊无险。 <br />
<br />
美国媒体在报道虐俘丑闻时，可以说是争先恐后。阿布监狱丑闻最早的报道者中，就有美国CBS电视台和《纽约客》杂志。之后美国各个媒体掀起了声讨政府的热潮，04年起，《华盛顿邮报》、《纽约时报》等详细报道了监狱里的审讯技术及关押犯的悲惨状态，并呼吁政府尽早关闭关塔那摩。而《时代》周刊干脆发表了几十页的卡塔米审讯日志。 <br />
<br />
与此同时，各个民间人权组织也开始积极行动，捍卫“敌人的权利”。其中最著名的是纽约的“人权观察”，它对关塔那摩的囚犯状况做长期的跟踪调查，推出系统的调查报告。“宪法权利中心”这个NGO，则给卡塔米这样的人提供律师帮助。与关塔那摩相关的书籍、音乐、话剧、电视片、游行示威纷纷出现，高校、教会、电台、电视台对关塔那摩的讨论层出不穷。 <br />
<br />
在舆论压力下，立法机关开始有了反应。“反虐俘”最著名的代表是共和党参议员麦克凯。他说，“为了赢得这场反恐战争，我们不仅需要军事上的胜利，而且需要价值上的胜利，虐俘让我们在价值上损失重大。”05年10月，参议院以压倒优势通过反对虐俘的法案，“禁止对战俘使用残酷的、不人道的和污辱性的惩罚”。压力之下，布什于12月签署了这个法案，以示“美国政府反对虐待，尊重国际法规”。 <br />
<br />
司法的力量同样不可忽视。04年，最高法院判决关塔那摩囚犯有权挑战他们的被关押状态。06年6月，判决关塔那摩囚犯确系日内瓦协议的保护范围，同时还判决，政府不能另设行政军事委员会来审判犯人，审判必须通过常规法庭或者军事法庭。 <br />
<br />
即使是布什政府，也从没有公开提倡过“虐俘”。对某些温和的刑讯逼供，它可能曾经“睁一只眼，闭一只眼”，但随着社会压力的增强，它不得不一再站出来表态反对虐俘。其实，布什政府也的确有为难之处。要知道，如果另一次“9・11”发生，需要负责的可不是《纽约时报》或者“人权观察”，而是美国政府。更令某些官员想不通的是，某些伊斯兰极端组织正在砍下无辜美国人的头颅时，对准恐怖分子大声放音乐竟然都被指责为“暴行”。 <br />
<br />
然而，正如麦克凯所说，“我们是比我们的敌人更好的人”。文明社会必须用更高的标准来要求自己。在这个标准下，阿布监狱的虐俘者受到了应有的惩罚，有两个美国士兵甚至被判处了10年和3年的徒刑。美国驻伊总指挥官桑切斯也称，是阿布监狱丑闻导致他“被迫退休”。与此同时，关塔那摩的囚犯正一批批地被释放，就是卡塔米，据最新消息说，由于他曾受虐待，美国很可能无法起诉他。 <br />
<br />
这些斗争表明，关塔那摩那700来个人的痛苦并没有白白承受。他们的痛苦，已经被美国社会转化为强化其人权保障机制的信号，从而避免更多这样的痛苦。毕竟，泱泱大国的运转中，政府不可能不出错，重要的是它如何面对自己的错误，是否承认并改正错误，是否在一个更大的政治框架中受到制约。同样重要的是，这个社会能否容忍政府以“国家安全”名义践踏人权――无论是“我们”的人权，还是“敌人”的人权――归根结底，人权是人类的权利，不仅属于我们或者他们。</div>

							
							
							
							
