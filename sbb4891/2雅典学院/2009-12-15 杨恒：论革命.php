																											<div id="postmessage_374152" class="t_msgfont">按语：小波被重判11年，大家有了争论。这个问题――改良与革命，争论了几<br />
十年了。只在口头上讨论革命和改良是没有意义的，实际上，真正的革命和改<br />
良是一体的，激烈的改良就是革命，渐进的革命就是改良。争论这些是无谓的，<br />
关键是坚韧不拔的做。今天的公民运动，就是一场根本的体制性的革命，但是<br />
其手段则是崭新的------非暴力的、理性的、公开的。难道革命只应该是暴力的、<br />
狂热的和密谋的吗？今天我们在开创中国历史的一个崭新的时代，公民是这个<br />
时代的主体，而公民运动则是这次变革的唯一能够成功的道路！---杨恒<br />
<br />
<br />
<br />
在公元２０1０年将至的时刻，中国已经走到了历史的关口，我们是继续忍受<br />
旧时代的一切，还是用决然的勇气进行一场新的革命，这对于我们的种族和国家<br />
而言，是一个关乎生存与毁灭的命运抉择。<br />
<br />
&nbsp; &nbsp; 没有暴君与暴政就不会有革命<br />
<br />
&nbsp; &nbsp; 几千年以来，中国人民承受了无尽的灾难，暴君与暴政几乎占据了我们文明史<br />
的全部内容。所有的独裁者 都是暴君。在暴君眼里，人民过去是、现在是、将来仍<br />
然是任其奴役的草民、贱民；在暴君心里，人民根本就不是人，根本就不配享有人<br />
的权利。所以那些视自己为天之骄子的暴君们对我们这个种族的人民从来都是穷凶<br />
极欲、为所欲为，遭战乱驱百姓为炮灰，享太平则役人民为牛马。一方面对人民残<br />
酷剥削与压迫，另一方面又要人民称颂他是万世的圣明。这就是所有暴君的嘴脸。<br />
暴君是万恶之源，他们造就了人世间的所有不幸、痛苦和灾难。暴君酷爱暴力，常<br />
常炫耀暴力以恐吓人民，常常实施暴力以维持统治，他们使得整个社会都充满了暴<br />
力的血腥。<br />
<br />
&nbsp; &nbsp; 独裁者的统治就是暴政。即使人类进入了现代文明，暴政依然肆虐无度，与以<br />
前唯一不同的是，暴政学会了伪装。在各种冠冕堂皇的名义之下，暴政在公开横行。<br />
二十世纪，人们目睹了一幕幕令人发指的罪恶暴政的惨象。斯大林以“布尔什维克”<br />
的名义屠杀了俄罗斯民族的所有精英；希特勒在“雅利安人种至上”的叫嚣中几乎<br />
灭绝了犹太民族；毛泽东以“革命”的幌子残酷地杀戮了难以数计的无辜同胞；波<br />
尔布特对自己民族的灭绝竟是为了进入“共产主义”......面对顽固而凶蛮的暴君，<br />
一切哀求、呼吁、改良都将徒劳无功，只有进行彻底的革命，人民才能从压迫和奴<br />
役中得到真正的解放。<br />
<br />
&nbsp; &nbsp; 革命不是暴乱，革命党人不是暴民<br />
<br />
&nbsp; &nbsp; 暴君造就了无数个暴民，暴民必然是未来的暴君。杀人放火、当或想当皇帝者<br />
就是暴民。驯顺的“贱民”们在做奴隶而不得时，他们就会揭竿而起，以暴易暴，<br />
做放火杀人的暴民。暴民造反的成功并不会给人民带来真正的幸福与安宁、自由与<br />
民主，他们带来的只是改朝换代的血腥和夺权后的片刻狂欢。成功的暴民还会同他<br />
的前任一样肆无忌惮地奴役人民。朱元璋、洪秀全、毛泽东那类暴民上台前不管怎<br />
样沽名粉饰也掩不住上台后暴君的骨子和嘴脸，他们演出的都是中国历史中最常见<br />
的场面──改朝换代、换汤不换药的专制轮回。难道我们的种族永远无法摆脱这种<br />
悲惨的宿命吗？难道我们的人民永远只能在“打天下坐天下”的无耻而血腥的游戏<br />
中默默地生、痛苦地死吗？只有革命才能彻底打破这种轮回与宿命。真正的革命绝<br />
不是“肚皮”（活不下去）引导的农民造反，也绝不是“屁股”（要当皇上）引导<br />
的宫廷政变，而是“头脑”（人类的理想）引导的变革制度的社会运动。革命党人<br />
不是暴民，不是阴谋家，他们是自由民主的战士。<br />
<br />
&nbsp; &nbsp; 革命与反革命<br />
<br />
&nbsp; &nbsp; 五千年的苦难祖国只有过一次、唯一的一次真正的革命──孙中山、黄兴、宋<br />
教仁的革命。那场为共和、为民主、为人民的革命，那场高尚而无畏的革命惊天地<br />
泣鬼神，它彻底地终结了中国几千年延续不绝的帝制，建立起亚洲的第一个共和国<br />
──中华民国。从此皇帝就成了被中国人民永远唾弃的称号，那些天天做着皇帝梦<br />
的暴君们也决不敢将索命套往自己头上带，因为袁世凯已经为他们实践了一次。这<br />
就是那场真正革命的伟大意义，它在中国从根本上建立了共和体制，并明确了人权<br />
自由与宪政民主是共和国的永恒目标。<br />
<br />
&nbsp; &nbsp; 革命党人以大无畏的英雄主义气概用道义的铁肩承担了一个民族的苦难，由此<br />
“革命”一词在中国就拥有了无以伦比的感召力。从此中国大陆就出现了许多光怪<br />
离奇、荒诞无耻的假革命、伪革命和反革命。他们借革命之名攫一己之私，行专制<br />
独裁，革命只是他们披到自己身上的外衣，看看他们的行径哪一宗不是赤裸裸的反<br />
革命──袁世凯的窃国称帝、毛泽东的独裁专制个人崇拜，他们的行径和宫廷政变、<br />
暴民造反没什么区别，只是他们比以前的阴谋家、暴民更狡猾、更残暴、更善于伪<br />
装而已。独裁者必然是革命的敌人，是彻头彻尾的反革命。<br />
<br />
&nbsp; &nbsp; 我们蔑视、唾弃那些打天下坐天下、共别人家产成自己家业、当官做老爷的所<br />
谓“革命”，它们都是对革命的亵渎，那是不折不扣的伪革命、反革命。我们需要<br />
的是──华盛顿、杰斐逊的革命；是罗伯斯庇尔、马拉的革命；是十二月党人的革<br />
命；是孙文、黄兴的革命；是制度的彻底变革，而不只是统治者的更迭。<br />
<br />
&nbsp; &nbsp; 我们的革命──中国的第二次革命<br />
<br />
&nbsp; &nbsp; 我们的革命是一场“顺乎天意、应乎人道”的社会变革运动，是共和革命的继<br />
续，是中国的第二次革命，是要驱除一切独裁者，建立起符合人类共同价值标准、<br />
捍卫人权与自由的真正民主制度。相对于独裁专制的旧体制，我们的革命就是要使<br />
奉行“人人生而自由平等”、“主权在民”、“权力分立制衡”伟大原则的民主制<br />
度得以根本确立。从这个意义上讲，革命通向自由与民主、安宁与幸福，它坚决拒<br />
绝一切带给人民痛苦、不幸与灾难的专制制度；而革命党人则是一切独裁者的最无<br />
情、最坚决、最不妥协的敌人。<br />
<br />
&nbsp; &nbsp; 非暴力是革命的梦想<br />
<br />
&nbsp; &nbsp; 千百年以来，人们在向和平文明迈进的途中一直在与野蛮与暴力进行着艰难的<br />
斗争。暴力一直是暴君们最为钟爱的武器，暴力能使帝王的野心、荒诞的幻觉成为<br />
现实，这就是暴力的魅力。人类为此付出了惨重的代价。<br />
<br />
&nbsp; &nbsp; 然而人性和良知是不会为暴力所灭绝的。在大英帝国统治下的印度，圣雄甘地<br />
引导印度人民进行的伟大的非暴力主义实践，从根本上改变了人类的观念，改变了<br />
历史的进程。甘地的胜利展示了人性与良知的感召力，表现出非暴力的强大的生命<br />
力。在民众的觉醒、支持与参与下，非暴力开始取得一系列辉煌的胜利。甘地为人<br />
类所开启的这扇非暴力之门正在将人类引向自由、和平、幸福与宽容。从此非暴力<br />
如同黑夜中的灯塔指引着人们。<br />
<br />
&nbsp; &nbsp;公元１、９、８、９年，在中、共、统、治下的中国大陆，爆发了一场追求民、<br />
主的伟大的非暴力运动。中国学生与民众以高尚的良知勇敢实践着甘地的非暴力主<br />
义原则。那场彰显天理民心、弘扬人性良知的非暴力运动最终遭到了独裁者的坦克与<br />
机枪的血腥杀戮。二十年来，独裁者利用手中的强权刻意将屠杀手无寸铁同胞的人间<br />
惨剧包装成镇压所谓“反革命暴乱”的无耻闹剧。在墨写的谎言下，中华民族的心智<br />
被愚化、弱化、恶化与丑化。当那些刽子手们看到年轻人不了解他们在十年前杀戮自<br />
己同胞的暴行时，阴暗猥琐的犯罪感转化成自欺欺人的窃窃自喜，人性与良知再一次<br />
在暴力与谎言下被公然地蹂躏和强奸。<br />
<br />
&nbsp; &nbsp; 面对这一切，我们有责任、有义务向世人昭示：我们将一如既往地延续十年前<br />
死难同胞所献身的事业，我们将永远恪守心中的良知，我们绝不与暴政进行妥协，<br />
我们也绝不向暴力屈服。我们将大声疾呼，我们将勇敢战斗，我们永远是一切暴政<br />
最坚决的敌人，我们将在革命的旗帜下勇敢地推进祖国大陆的民主和人权事业。<br />
<br />
&nbsp; &nbsp; 革命党人的抉择<br />
<br />
&nbsp; &nbsp; 对于这个腐败、霉烂、黑暗的旧世界，假如我们仍没有勇气突破自己种族的怯<br />
懦与惰性，那么我们注定就要承受被奴役与失败的宿命。委屈求全、抱缺守残、忍<br />
气吞声、任人凌辱一直是我们种族的常态，这使无数暴君敢肆意妄为，把人民碾在<br />
泥里、踏在脚下，而所有的苦难均来自我们自己的懦弱、自私、无知与短志。祖国<br />
在亟切地呼唤着一批坚韧不拔、天下为公、志愿为中华民族之福祉而献身、为自由<br />
民主而奋斗的革命党人。只有他们才能以大无畏的勇气引导我们的种族与邪恶的暴<br />
政进行决然的战斗，只有他们才能以革命手段回击暴政的压迫。<br />
<br />
&nbsp; &nbsp; 革命党人是绝对厌恶暴力的，即使面对最残暴的敌人，革命党人也不轻言暴力，<br />
但是革命党人并不害怕流血，也绝不会放弃自卫与起义的权利，有什么样的暴政就<br />
会爆发什么样的革命。今天，祖国面临着危急的局面，革命党人绝不能懈怠与暴政<br />
之间的战斗，只要中国还存在独裁专制、存在奴役压迫、存在政治迫害，那么，革<br />
命党人就永远不会停止战斗。当独裁者阻绝了一切通向和平的非暴力的出口时，当<br />
人民一切善意的呼吁永远只能面对监狱与屠杀时，革命党人就会用自己的鲜血为人<br />
民的正义呼声勇敢地杀出一条血路，以决然的手段进行一场消灭暴政的革命──这<br />
就是革命党人的必然抉择！<br />
<br />
&nbsp; &nbsp; 由于不可抗拒的召唤，革命党人别无选择<br />
