<br />
为什么我们必须要灭掉儒教？<br />
在此，我郑重的告诉大家。这是中国真正走向民主化而不是重回专制轮回的唯一出路，也是让中国人真正拥有做人权利的唯一方法。西方文明，一直向着“人与人生而不平等”，“人性本恶”并就此作出限制的正确方向发展，而儒教“文化”所走的道路，却是与马克思邪教，纳粹主义如出一辙的“人人平等，社会大同”的追求，“人性本善”的邪恶方向发展，致使中国社会走上了“圣人治国”的邪路，更队日后中国沦为马克思邪教的实验修罗场负有不可推卸的责任。我们应该看到，西方文明，虽然走上过“中世纪”的弯路，但他们的大方向确是正确的，并独立的发展出了民主宪政的先进制度，与自由主义先进文化。而中国的“国学文化”，却因为大方向的错误而走向了黑暗，最后必然走向自我毁灭之路。事实上历史上，一切背弃“人与人生而不平等”，“人性本恶”方向的文化，必然走向毁灭。（拜火教文化，印第安文化等）<br />
为什么我们的“”中国文化“是邪恶的，文化是由人来传播的。古代西方文明的传播者主要是宗教人士和古希腊的哲学家。他们的共同特点，就是他们传播的文化意志不受现实中的专制暴君的控制。也就是说，他们拥有较大的思想自由。宗教人士信奉的“上帝”，和希腊先哲信奉的“真理”，都成为他们抵御某些专制暴君的文化管控制度的精神武器。<br />
而中国，传播“儒学文化”的“文人”是一帮什么东西呢，古代中国文化的传播者是“士”阶层。”“士”属于周礼的“等级”――“天子，诸侯，大夫，士，庶民”中的第四等级，一方面他们是大夫家中的家臣，另一方面，他们也同样是“庶民”头上的最底层的“统治者”。中国的“文人”从一开始就是世俗权力系统中的一环，他们心中既没有宗教的信仰，也没有对真理的追求，他们的心中只有对虚无缥缈的“天命”的追求，和对“大人乃至大人的进化版本――圣人'的无限忠诚。在《论语》之中，圣人是以“尧、舜、禹、汤、文、武、周公”的面目出现的；实际上无论“大人”，“圣人”，他们全都是专制独裁者的化身。 <br />
很明显，在古代中国，文化的传播者”士“，是一群专制独裁者豢养的”高级奴仆“，一群不知真理，不知信仰为何物的精神奴隶。从一开始，他们便既丧失了自然平等的权利，也丧失了社会的自主的权利，更丧失了精神自由的权利。中国的文化，说白了，从一开始，就是由这样一帮丧失了自然的平等、自主的主体和自由的精神的“文人”们“创造的”。春秋战国时代的诸子百家，基本上就都是由这样一类称作“士”的“文人”，例如像老子、孔子、墨子、孟子、庄子、荀子、韩非子，等等等等组成的。他们中的所有的人们，都不可能逃得了世俗政治强人们的暴力的恐吓和威胁。正是因此，他们的“文化”的创作，全都受到了极大的不平等、不自主、不自由的严重的局限。其中，由以儒教对专制独裁者的”文化服从“最甚，鉴于他们不知人性究竟为何物，盲目的相信一种明显带有斯德哥尔摩综合症倾向的”会有圣王带领人们走向大同“的”信念。他们创造出了以”伦礼“为核心的一种维护专制独裁的”文化“――儒学。这种儒家的文化深深地打上了“人人生而不平等”的永恒的历史烙印。尤其在汉武帝把孔子及其儒家定于“独尊”的地位之后，历代统治者更是层层加封――“圣人”、“文宣王”、“至圣先师” 、“万世师表”，等等等等，使之完全垄断了中国的“文化”，从此，中国文化的传统，事实上就只能是中国儒家文化的“传统”。把持中国文化的“文人”，就成为了拥护儒教邪说的儒教徒的代名词。而这些儒教徒一直在把中国文化的大方向完全定向成了“人人生而平等”，“期待圣王治天下”的邪恶的方向的文化”。请问，对于这样的“文人”，中国人能够继续让它随心所欲地永远地把持着我们中国文化的发展方向向邪道永远的走下去吗？不能，既然不能，我们当然便只能义无返顾地彻底地对这个邪恶的文化进行毁灭了。 <br />
自孔子，孟子，荀子，到董仲舒，二程，朱熹，王阳明，乃至今日的“新儒家”的“学者”，文人“们，无不是妄图控制中国人思想，把中国文化方向定向成“人人生而平等”，“期待圣王治天下”的邪恶的方向的文化”的儒家邪教“文人”。他们或有意或无意，都成为了让中国“文化”走向死路的凶手罪犯。因此，灭绝儒教，杀死一切儒教徒，铲除他们的邪恶影响。烧了他们的“邪书”，“洗脑教材”，拒绝他们鼓吹的“中华国学文化”邪路。是二十一世纪中国人最重要的任务之一。<br />
