土共说, 看民运猖狂到几时.<br />
<br />
结果土共猖狂了60多年, 还要继续猖狂;<br />
<br />
民运打89起, 猖狂了不到20年, 现在越来越半死不活了;<br />
<br />
喊口号没用, 拿出办法来. <br />
<br />
<br />
我的看法是：<br />
其实说到底，还是因为民运（包括现在本坛的某些民主人士）都是不折不扣的书呆子，“学院派”知识分子。这些人搞政治，要么最后成为真正的政治高手的傀儡（比如张澜），要么把命都给丢掉（比如储安平）<br />
你说这些知识分子傻吧，他们的大道理一堆一堆的。你说他们聪明吧，这些人连中国人的基本民族性也没搞明白。老百姓要的是眼前利益，是实实在在的钞票。不是什么”普世价值“。（虽然从长远角度看，普世价值对人民是百利而无一害的）。这帮人不从自己身上找失败原因，还总是把责任赖给老百姓，整天叫嚣”开启民智，宣扬民主宪政思想“。连最基本的”老百姓永远是短视的“这个道理都不明白（或者说不愿明白）。<br />
”人民群众“是不是笨蛋？从理论上说，当然是。但是大多数人都是笨蛋，而少数理论上的”聪明人“至大多数人的愿望于不顾。那么，他们怎么可能会指望得到大多数人的支持？<br />
人民想要钞票，”民主精英“却给他们几张他们看不明白的破纸（08宪章等），擦屁股都嫌硬；相反的，TG呢？TG一面让百姓生活在贫贱中，一面又创造了一个实实在在的”特权阶层“神话，吸引着穷老百姓们对富裕，地位的追求。就像给一头驴眼前栓了一个胡萝卜，驴为了得到眼前的萝卜，必定会跟随TG的指挥棒。你去告诉驴――我这边有更多的胡萝卜，有用吗？你的胡萝卜，能让驴看见算啊！<br />
其实解决这个问题的方法很简单，老百姓想要钞票，那就亮出招牌――跟我走，给你们更多的钞票，我们能比共产党满足你们更多的愿望！老百姓从你那得到了实实在在的好处，再将”破纸“顺手塞给他们。他们会不要吗？告诉驴你有更多的胡萝卜，没用。你把几个胡萝卜摆在驴的眼前，它们会不跟你走吗？<br />
当然啦，”以燕归来，十岗为代表的，百无一用专坏事的“民主精英”可能会发出声嘶力竭的嚎叫――你这是民粹主义思想，是反民主的！你是流氓！！<br />
可是想当年，胡适们不是也这么喊得吗？为什么自由民主的胡适们，没有干得过共产共妻的毛泽东们呢？<br />
不知道反省的人，有出息，会成功吗？<br />
民粹不是坏东西，它就是一种手段，是正是邪，就看你怎么用。<br />
时间有限，就讲这么多。欢迎大家提出自己的意见。</div>

							
							
							
							
