													<h2>寡头统治的弊端</h2>
						
						
																											<div id="postmessage_687045" class="t_msgfont">　　寡头统治看似不错，它既防止了独裁的偏执，又避免了民主的“乱象”。从人类历史上看，寡头统治也自古就有。但是，这却是比较弱的一种形式。为什么呢？一般而言：<br />
　　民主政治如果和当时的社会现实相吻合，那么就会比较强。比方说：希腊民主和其军事制度吻合得很好，现代民主制度则和资本主义比较吻合。<br />
　　独裁统治可以办成大事，当然也可以坏大事。这个在现代就太危险了：古代反正人命不值钱，物质基础很差；现代大批死人、物质上一落到底的风险，谁能承担得起？<br />
<br />
　　寡头统治的问题在于：<br />
　　如果它不彻底，那么它只是独裁统治的一种表象，一旦危机发生，个人独裁统治的实质就显露出来了。这样一来，它实际上也不是寡头统治。照样不能避免坏大事。<br />
　　如果它很彻底，那么它根本就经不起危机的考验。因为没人敢负责任，人性如此。对这一点不理解的人，可以去看看《我的奋斗》之类的读物。这是历史所证明的事。<br />
<br />
　　无论是民主政治还是独裁统治，在必要的时候，都可以把权力赋予一个核心人物。这对于独裁统治来说，理所当然；对于民主政治来说，则比较危险，但只要有制度保证，形势不是太不利的话，过后还是可以恢复常态。<br />
　　但寡头统治不行，寡头统治和民主政治的区别在于：人民是确定的那一群人，制度和习惯可以是针对这群人的保障，在此基础上，政策就可以比较灵活；而寡头的组成显然不是一成不变的，如果要维持寡头统治，就必须特别慎于变化，这就使得体制丧失了抗拒危机的能力。<br />
　　当然，没有危机的话，无论哪种政治，表现得不会太差。</div>

							
							
							
							
