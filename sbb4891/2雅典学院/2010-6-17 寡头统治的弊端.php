一、柏拉图（Plato）以一人之统治为君主政体，少数人之统治为贵族政体，多数人之统治为民主政体；而这三种政体之腐败形态，则依次为专制政治、寡头政治与暴民政治。 <br />
<br />
二、墨克依完（MacIver）有曰：多数或一般人民从来未统治过，而真正统治之任务经常是掌握於少数人之手。所谓宪政体制问题，乃为一人对少数人之关系，尤其是少数统治者对多数被统治者之关系。在这种意识上，是否少数人均对多数人负责？否则，那便是寡头政治。 <br />
<br />
三、韦柏新世界美语辞典之解释：寡头政治乃系一种政治制度，其统治权为少数人所把持。亦有谓寡头政治乃一国之统治权掌握於少数人之手者。要而言之，凡属一个国家系由少数人所统治，或一种政体系为少数人所把持者，均可称之为寡头政治。像古代斯巴达（Sparta）虽有二王，实为一寡头政治之国家。 <br />
<br />
四、韦柏新国际词典指出：寡头政治是一种由少数人所统治之政治；一种政府之形态，其权力乃操在少数人之手，或一个国家系由少数人所统治。通常寡头政治与贵族政治有别，因为前者系一种少数人为了贪渎或自私为目的之统治。 <br />
<br />
五、哥伦比亚百科全书则曰：寡头政治是一种由少数人所统治之政治。如果这种统治之少数人均被认为比较其他的人为优秀，而且最适宜於统治，则此种政治为贵族政治。又寡头政治一词含有几分轻蔑之意，而那些均为寡头政治之多数政府，事实上，会为其自己使用其他名称及遵守某些其他制度之外表形象。</div>

							
							
							
							
