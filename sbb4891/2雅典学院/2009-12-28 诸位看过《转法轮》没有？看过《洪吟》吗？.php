													<h2>诸位看过《转法轮》没有？看过《洪吟》吗？</h2>
						
						
																											<div id="postmessage_400892" class="t_msgfont">某些人讨论轮子喜欢信口开河。有些人甚至连轮子的圣经《转法轮》都没看过。我觉得总该先了解别人的意思，才能找到共同的话语系统。<br />
<br />
转法轮有十多种语言的版本。我的朋友茅山道老师曾经钻研过轮子经典，还亲自修炼过很长时间的法轮功。<br />
<br />
茅山道老师说：《转法轮》除了中文版的，其他的版本语言都非常优雅，因为都是知识分子翻译的。只有中文版的文字算不上高雅，因为那是根据录音整理编辑的，里面的东北话词汇、东北话语法特多，怎么看都不优雅，整理人还不敢擅自润色，所以总体上保持了原汁原味的李老师口语风格。<br />
<br />
以前李老师每次讲法的内容都稍有不同，正如释迦牟尼每次讲课的内容都不一样。后来李老师觉得需要有一本作为经典的统一的教材，就有了《转法轮》。<br />
<br />
能够见证一种新兴的宗教在我们身边是如何发展壮大，又如何被迫害，如何被妖魔化，是一件很荣幸的事。了解法轮佛法，能够让我们对于人的信仰有更深的认识。<br />
<br />
我们还经常能听到教主本人的声音，看到他的音容笑貌，这是何等的幸运。<br />
<br />
李老师是一个诗词爱好者。他的《洪吟》就是他的诗歌小集。艺术性虽然不能跟《双照楼诗稿》，至少也抵得上《李肇星诗集》。所谓“诗言志”，说话可能隐藏的潜意识，往往会在诗中表露出来。<br />
<br />
我建议对轮子有兴趣的朋友看看这两本书，全面了解一下李老师的思想，这样不至于跟轮子们找不到共同语言。<br />
<br />
轮子并非一个蔑称。按照茅山道的说法，孔子，老子，墨子，庄子，轮子，都是子，是尊称。</div>

							
							
							
							
