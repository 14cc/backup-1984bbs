													<h2>搞清楚64学生运动的性质很重要</h2>
						
						
																											<div id="postmessage_372394" class="t_msgfont">我是六四过来人。那时的我，与比广场上的学生娃子相比，已经可以是大哥了。我觉得直到今天，他们这些当时还算是孩子们的勇气，都没有后来者可以相比。而那批人中的佼佼者，比如刘晓波，直到今天，依然是敢为理想献身的勇士。六四失败虽然有很多的历史原因，但是当时的主要社会民众力量，工人，他们的冷漠，置身事外的超脱姿态，是我当时很不爽的强烈感觉。九十年代工人大批下岗，成为社会弃儿，卢瑟，我认为是报应，是活该。这种自私短见的国民性是民主运动悲剧的主因之一。<br />
=======================================================================================================================================<br />
我只凭我能掌握的资料得出我的观点，有不合胃口的地方，请你及各位谅解。64不过是一场学生爱国运动，不是民主运动，我的理由：<br />
1、他们是天安门广场纪念胡耀邦的续集扩大化，不过是被人忽悠出校园的，不是自己事先有组织、有计划地争取民主自由的示威游行。<br />
2、他们喊的民主，不过是共产党给他们的观念――当家做主。当家――父为子纲，做主――君为臣纲，这能叫民主吗，这也说明他们对民主的认识是有限的。<br />
3、他们反对的仅仅是贪污腐败问题，还没有深刻认识独裁统治的本质，他们只是希望通过呼喊，寻求对话，让当局改良。所以，不能和社会个阶层达成共识。<br />
4、他们有热血和激情，但不能代表任何社会阶层的利益，没有反映出社会阶层的诉求，所以北京市民、工人的支持，也仅仅能看作恻隐之心。<br />
5、他们是一群没有领袖的乌合之众，高自联也是临时拼凑的草台班子。吾尔开西，一个新华门示威事件中勇敢的大男孩，回到广场给大家传个话，就被众人推作了领袖。柴玲，一个爱出风头的小姑娘，经常带着大家喊喊口号，就喊成了领袖。<br />
<br />
不要怪工人和大众，是学生们主动把他们排除到运动之外的，他们是社会的底层，没有理想、没有激情，但他们有很具体的利益，就是家庭――上有老、下有小，这也是他们支持学生的原因。当时有一个细节，就是向毛主席画像扔颜料的3个湖南农民青年，居然被学生们送进了派出所。这个镜头很重要，这表明学生是听党的话的，不过是在听党的话的前提下，有点牢骚要发，如果让他们发完牢骚，就表明学生们争取到民主了。再说了，在坦克和机枪的面前，没有血肉相连的利益联合，他们凭什么要去送死。比起这些市民，那些文人知识分子，才是最值得鄙视的。假模假样的在广场上支持，惺惺相惜的演说，当军队出现的时候，广场上就只有学生以及寥寥可数的几位老师。而且，事后也不见几位名士和精英分子出来指责共产党的暴戾行为。</div>

							
							
							
							
