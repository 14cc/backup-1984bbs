													<h2>韩寒的《独唱团》注定又是一个山寨</h2>
						
						
																											<div id="postmessage_372003" class="t_msgfont">《独唱团》注定又是一个山寨<br />
<br />
2009-11-22 11:44:22<br />
<br />
现在是11月底了。按照韩寒的承诺，他主办的杂志《独唱团》就要面市了。然而，它仍然是犹抱琵琶半遮面，千呼万唤不出来。光打雷不下雨，正反映了这本杂志未来的困境。<br />
<br />
据说，《独唱团》每期的发行量将达30万册，而其内容不过是文学作品为主。因此，我并不看好这本杂志。众所周知，中国的文学是非常糟糕的。我已经20年不看国内的小说了，包括韩寒的在内。前不久，我无意中翻阅了近来的某一期《小说月报》，上面还有韩少功等“名家”，然而，我的感觉是：简直惨不忍睹。大家都清楚这么一个事实：中国人的现实生活，比中国人的小说中写的生活，不知道要“精彩”多少倍数。还看什么小说？哪怕是CCTV，上面的那些面孔，小说中的刻画恐怕也不及电视上的1/10。<br />
<br />
韩寒的另一个致命错误是逆“社会主义市场经济”的价值规律而动。他想改变中国作家上不了胡润富豪榜的现状，所以，《独唱团》的稿酬开出了每千字不低于1000元的天价。不无遗憾的是，高稿酬的时代已经成为过去式；况且，中国还没有这么值钱的作家。人为的拔苗助长，是不可能成功的。<br />
<br />
不过，将要导致《独唱团》困难重重的根本原因，是我国的文化审查制度。只要这个制度存在着，一切文学艺术作品都将失去市场，除了那些貌似“文艺”的东西之外。什么“二人转”，什么“不差钱”，什么赵本山，什么宋祖英，才是中国“文艺”的市场标杆。<br />
韩寒那本杂志的刊名，从“文艺复兴”变成“独唱团”，本身就是我国文化审查制度失败的一个表现。其实，任何文化审查制度都是注定了要失败的。1841年，中国的老祖宗马克思写了一篇论文《评普鲁士最近的书报检查令》，大力抨击书报检查制度。他认为“书报检查制度是不能成立的，因为它要惩罚的不是过失而是意见”。出版自由并非没有“弊端”，不过，“不耐心地对待出版物的短处，也就不可能利用它的长处。不带刺的玫瑰是没有的！”<br />
<br />
“法律仅能涉及人的外部行为，而不能涉及人的思想”。因此，马克思大胆认定“反对倾向的法律，即没有规定客观标准的法律，乃是恐怖主义的法律”、“凡是不以行为本身而以当事人的思想方式作为主要标准的法律，无非是对非法行为公开认可”。马克思认为荒谬的书报检查制度是徒劳无益的事情，它不可能达到“检查”的目的。“书报检查制度本质上是建立在警察国家对它的官员那种虚幻而高傲的概念之上的。公众的智慧和善良意志都被看做甚至对最简单的事物也是无能为力的东西，但对于官员来说，却连不可能办到的事情也被认为是可能的”。<br />
<br />
新闻出版总署之所以毙掉《文艺复兴》，我的推测是，首先，他们认为中国的文艺已经足够繁荣，所以是不需要什么“复兴”的；其次，他们认为“文艺复兴”不是“人类优秀文明成果”，而是“资产阶级自由化”的表现，所以，“文艺不能复兴”；最后，他们认为“文艺复兴”可能会与“主旋律”唱反调，所以，应该予以阉割。<br />
<br />
可笑的是，新闻出版总署居然通过了“独唱团”这个刊名！“独唱团”是典型的反讽，典型的双关语！一方面，中国的社会精英们在自弹自唱，以致弹唱成了世界上独一无二的“利益集团”；另一方面，中国的草根阶层也在自弹自唱，然而，这是一群找不着北、没有出路的弱势群体。两个“独唱团”，都是“大而无当”的。<br />
<br />
黄仁宇先生曾经说过，中国“上面是一块长面包，大而无当，此即是文官集团。下面也是一块长面包，大而无当，此即是成千上万的农民，其组织以淳朴雷同为主。中层机构简单，……上下的联系，倚靠科举制度。”他称中国社会是一个“潜水艇夹肉面包”。真是血肉横飞的譬喻！<br />
<br />
《文艺复兴》变成《独唱团》之后，这本杂志的定位就不应该再是什么“青年知识分子”了。毙掉《文艺复兴》，体现了这样的一个意图：中国的知识分子只能“天天向上”，不要妄图什么“启蒙”了。中国的知识分子应该成为社会精英的一部分。事实上，他们已经升级换代，而且的确成功了！可怜的草根阶层们，你们就继续自弹自唱吧。你们再怎么抱团，也是“大而无当”的废物。这造就了《独唱团》的尴尬局面：中国的普通百姓和草根阶层，其实永远不可能称得上是什么独立的“团”，只能算是乱作一团的“肉饼”。<br />
<br />
将“文艺复兴”变成“独唱团”，那意思就是：阳春白雪的领导喜欢看下里巴人的“二人转”。所以，我认为《独唱团》最终会成为一个“山寨”，它的唯一的生命力源泉就是：逃避监管。它只能在夹缝中求生存。好在韩寒相当洒脱。不久前南都周刊问他：“可是如果杂志卖不好怎么办？”韩寒的回答是：“不干了。”<br />
