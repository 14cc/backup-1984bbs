													<h2>为什么足球运动员不能诚实一点？</h2>
						
						
																											<div id="postmessage_714228" class="t_msgfont">来源Why is cheating OK in football? | Peter Singer | Comment is free | guardiancouk<br />
译者jays<br />
<br />
中英双语 <a href="http://article.yeeyan.org/view/105279/114163" target="_blank">http://article.yeeyan.org/view/105279/114163</a><br />
英语原文 <a href="http://www.guardian.co.uk/commentisfree/2010/jun/29/cheating-football-germany-goalkeeper" target="_blank">http://www.guardian.co.uk/commen ... -germany-goalkeeper</a><br />
<br />
<br />
德国队门将承认欺骗了裁判，但是球员不应该因为是在绿茵场上而逃避道德的批判。<br />
<br />
<img src="http://static.guim.co.uk/sys-images/Guardian/Pix/pictures/2010/6/29/1277802169960/Germany-v-England-2010-FI-006.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
弗拉克・兰帕德的射门明显越过了球门线，但是德国队门将曼努埃尔・诺伊尔装作不知道，继续将球扑出。照片来自 Getty Images 的 Cameron Spencer<br />
<br />
在 6 月 27 日英国队对德国队的世界杯小组赛进行到将近半场时，英格兰中场球员弗兰克・兰帕德一脚射门，打在横梁上，弹落到地面，明显地越过了球门线。而门将曼努埃尔・诺伊尔扑住球，将它拖回了赛场中。裁判和边裁――都还在跑向底线，站在无法准确裁决的位置――判定没有进球，比赛继续。<br />
<br />
在赛后，诺伊尔对他的行为作了如下解释：“我尽量不管裁判，专注于正在发生的事情上。我意识到球已经过线，我猜我如此之快地作出反应将它抱回骗到了裁判，让他误以为球没有过线。”<br />
<br />
简单地说：诺伊尔欺骗了裁判，并且洋洋得意。<br />
<br />
不管在哪种道德标准下，诺伊尔的所作所为都是错误的。难道诺伊尔正在进行足球比赛就表示唯一的道德规范是“不惜一切代价赢得比赛”吗？<br />
<br />
这似乎已经成为了足球界盛行的道德规范了。这些事件中最著名的是在 1986 年世界杯上阿根廷对阵英格兰的比赛中马拉多纳的一粒进球，他事后如此描述这次得分：“一部分归功于马拉多纳的头，另一部分归功于上帝之手。”比赛录像清晰无疑地显示了马拉多纳是手球得分的。20 年后，在一次英国广播公司的采访中，他承认他当时有意装作进球了，以此欺骗裁判。<br />
<br />
在去年 11 月份在法国和爱尔兰之间决定谁能闯入世界杯的一场比赛上又发生了类似的事件。法国射手蒂埃里・亨利用手控球传给队友，队友随即破门得分。当在赛后被问及当时的想法时，亨利说：“说实话，这是一个手球。但是我不是裁判。我这么做了，而裁判也没吹。这个问题你应该问他。”<br />
<br />
但真的是这样吗？为什么你能够逃避欺骗的处罚就代表了你是无罪的？球员们要是在球场上的所作所为可以逃避道德的批判，那么更甚一步，他们在球场外也就能逃避道德批判了，例如服用加强体能的药物。<br />
<br />
当今的运动的竞技性极高，奖金也很丰厚，但是这并不意味着就无法保持诚实了。在板球比赛中，如果击球手击出的球被一个外野手接到了，那么击球手就出局了。有的时候球被接到以后裁判不确定球是否碰到了击球板的边缘。击球手通常自己明白，一般，他会“walk”――离开赛场――如果他心里明白自己出局了的话。<br />
<br />
有的人依然如此。澳大利亚击球手 Adam Gilchrist 在 2003 年世界杯半决赛对阵斯里兰卡时离开了赛场，虽然裁判已经宣布他并未出局。他的决定让他的队友感到惊讶，但却受到了许多板球爱好者的热烈掌声。<br />
<br />
我在网络上搜索了一下，只看到一起足球运动员做出和板球运动员离场类似的明确事件。在 1996 年，利物浦射手福勒因阿森纳门将的犯规而获得了一个点球。他告诉裁判阿森纳门将并没有犯规，但是裁判执意让他踢点球。福勒只好从命，不过他故意踢得很轻，让门将轻松接住。<br />
<br />
为什么在职业足球运动员之中这样的行为如此之稀有呢？可能是因为极端的党派性的文化盖过了道德价值。球迷们并不在意他们的球队是否成功地欺骗了裁判，他们只会在对方球队欺骗时站出来反对。这是不道德的态度。（虽然，出于良知，以尼古拉斯・萨克奇为首的许多法国球迷都在亨利的手球之后对爱尔兰队表示了同情）<br />
<br />
是的，我们可以通过使用现代化技术或者视频录像来研究有争议的裁判判罚，以此在一定程度上解决问题。但虽然这样会减少欺骗的成功率，但无法彻底终结欺骗行为，而且它没能解决根本问题。我们不该给体育运动中的欺骗找任何借口。在一个重要方面来看，它比在私下里骗人更恶劣。当你这样做，你的行为会被上百万人的眼睛所记录，在无数的视频录像中被重演，还会在电视体育节目中被仔细分析，做该做的事，不要欺骗是特别重要的。<br />
<br />
如果诺埃尔停止比赛，告诉裁判这球进了，那么球迷们会有什么反应呢？因为足球界中此类事件少之又少，最初的反应毫无疑问是惊讶。一些德国球迷可能会感到失望。但是就整个世界来说――而且也包括每一个有良心的德国球迷――会承认他做了正确的决定。<br />
<br />
诺埃尔在上百万人面前错过了一次行道义的难得机会。他本可以给全世界观看比赛的观众，包括上百万易受感染的年轻人的面前树立一个正面的道德榜样。谁知道这样一个榜样可能会给那些看比赛的人们的生活带来什么样的变化呢？诺埃尔本可以成为一个英雄，为了正义而战。但可惜他却成为了又一个技术娴熟的足球骗子。</div>

							
							
							
							
