													<h2>不要以为在改革中强调公平，就一定是“乌有之乡”的同志</h2>
						
						
																											<div id="postmessage_286979" class="t_msgfont">语境和时代背景：毛已经把中国经济折腾到崩溃的边缘，邓力图恢复常态――这小学生以上的人都知道。否定文革但“宜粗不宜细”，要向前看――很快就被民间称为向“钱”看；对毛的评价放到二十年后――实际上就是维持毛的地位，也就是维持共产党一个党的领导。<br />
<br />
“共同富裕”……从来不是我党要考虑到事情，在邓的时代，这句话是为“允许”和“可以”做社会主义理论的合理性背书，在今天，是强调“我们”注意到了贫富分化，“我们”会防止进一步贫富分化的表态。……这个国家的事实清晰地表明，只要党的垄断权力牢牢在手，不但不会有自动共同富裕起来，而且无权的先富起来的人也会被权力剥夺财富<br />
――说得非常正确！<br />
<br />
不但不会有自动共同富裕起来，而且无权的先富起来的人也会被权力剥夺财富，这是在９２年之后才真正显现出来的。<br />
――五大公司的成立，说明在“强调的是‘可以’和‘允许’，即官员对老百姓挣钱不得干涉”的同时，权力寻租已经开始。<br />
<br />
据毛说（大意）：看一个政党，不但要看他的一时一事，还要看他的历史。<br />
<br />
中共的历史，从承继的前苏共，到井冈山，到延安，整个毛时代，以致到改革的邓时代，一脉相承的是从未以民众的疾苦为出发点。毛时代的土改（乡绅是中国历史上农村社会结构中可商讨的现象，但绝非“土豪劣绅”可一言蔽之）、大跃进到文革，自不用多说。邓时代，在维持毛的地位和共产党一个党的领导同时，开始了一个很重要的转变，就是从意识形态党转变为权势集团利益党。此转变在说“秃森颇，耐夫！”那个人手中完成。而二十年前是非常重要的转折点――其重要性为：权势集团不再遮遮掩掩（即不再使用“先富起来这种带有强烈时代特征的口号”）；知识精英大多卖身投靠；百姓则由“恨腐败”变为“恨自己不能腐败”。<br />
<br />
邓是相信只要不干涉，老百姓会自动共同富裕起来<br />
“改革开放的”演进的社会分化，邓根本就无法预见到<br />
――鬼才相信。<br />
<br />
不要以为在改革中强调公平，就一定是“乌有之乡”的同志。公平，只是乌有之乡同志们的招牌。</div>

							
							
							
							
