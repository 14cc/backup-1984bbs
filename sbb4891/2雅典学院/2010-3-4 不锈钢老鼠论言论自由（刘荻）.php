													<h2>不锈钢老鼠论言论自由（刘荻）</h2>
						
						
																											<div id="postmessage_502424" class="t_msgfont">不锈钢老鼠论言论自由（刘荻）<br />
2010-02-01<br />
<br />
言论自由是《08宪章》第11条。为了进一步宣传和推广《08宪章》，笔者撰写了本文，供大家讨论和批评。<br />
<br />
1.言论自由是消极自由。消极自由和积极自由这两个概念，是以塞亚&#8226;伯林在《两种自由的概念》一书中所提出的。消极自由是指不受他 人干涉的自由，只要没有他人干涉你，你就拥有消极自由，无论你是否有实践这种自由的机会和能力。而积极自由指的是实现自己潜能的机会和能力及个人实现特定 目的的能力。维基百科上列举了一些关于消极自由和积极自由之间的差别的例子，例如，你有出国度假或游过英吉利海峡的消极自由，因为没人阻止你这样做；但是 你没有这样做的积极自由，因为你买不起飞机票或者游泳游得不好。<br />
<br />
2.言论自由是消极自由，意思是说国家不能干涉你的言论；你可能没有行使 言论自由所需的能力和条件，但是这并不影响你拥有言论自由；聋哑人和痴呆症患者也有言论自由，虽然他们可能没有发言的能力，无法实践这一自由。言论自由是 消极自由，根据是美国宪法中有关言论自由的第一修正案，该修正案全文为：国会不得制定关于下列事项的法律：确立国教或禁止信教自由；剥夺言论自由或出版自 由；或剥夺人民和平集会和向政府请愿伸冤的权利。由此可见，言论自由只有一个必要条件，那就是国家不干涉你的言论。除此之外，言论自由不需要其他任何条 件。<br />
<br />
3.言论自由不保证每个人都有发言的能力和条件，也不要求国家用创办公营媒体等方式来确保每个人都有发言的机会。如果你不会说话或写 字，别人没有义务帮助你，好让你能够发言；如果没有媒体肯发表你的文章或者让你通过它们发言，你也不能以自己拥有言论自由为由要求别人为你提供发言的场地 和工具。无论是报刊拒绝刊登你的文章，电视台拒绝你亮相，还是BBS版主或博客的主人删除你的帖子或禁止你发言，都不是侵犯你的言论自由。因为这些发言的 “工具”是属于他人所有的，你没有使用他人的工具发言的权利，要使用他人的工具发言，必须征得工具主人的同意，满足工具主人提出的要求。同理，政府拥有的 媒体不发表反政府言论，并不是侵犯言论自由，但是政府禁止私人拥有媒体则是侵犯言论自由；政府审查私人媒体的内容属于侵犯言论自由，但是私人媒体的老板决 定自己媒体的内容则不属于侵犯言论自由。<br />
<br />
4.言论自由不是“说话被人听到”的权利，因为别人也有不听你说话的权利；认为人应该有“说话被人听到”的权利，其实就是想强迫别人听你说话。言论自由不保证你说话一定有人听。言论自由也不保证话语权平等，在一个自由社会中，话语权不平等乃是再正常不过的现象。<br />
<br />
5. 关于言论自由是否会导致资本家垄断话语权。既然言论自由不保证每个人都有发言的能力和条件，也不要求国家用创办公营媒体等方式来确保每个人都有发言的机 会，那么言论自由会不会导致话语权被资本家垄断呢？资本家拥有媒体，因此有权决定媒体的内容，但是应该注意的是，资本家拥有媒体的目的是为了赚钱，而不是 为了宣传自己的观点；而媒体要想赚钱，就必须迎合大众的口味，说他们想听的话。<br />
<br />
6.言论自由不保证所发表的言论一定正确或符合特定的道德或审美标准。恰恰相反，如果政府规定只能发表正确和符合道德标准的言论，那么言论自由也就不存在了。错误的言论、不道德的言论和丑陋的言论都有发表的权利。<br />
<br />
7. 言论自由不等于言论不需承担责任，相反，自由和责任是同一枚硬币的两面。国家虽然不能干涉你的言论，但是如果你的言论侵犯了他人的权利，给他人造成了实际 损害，你可能需要赔偿他人的损失。需要注意的是，诽谤漫骂之类的言论并不是一概没有言论自由或者会受到禁止，而仅仅是在侵犯了他人的权利，给他人造成了实 际损害的情况下，可能会被起诉要求赔偿。如果没有受害者起诉，或者受害者不能证明这些言论给自己造成了实际损失，那么即使是诽谤漫骂的言论也是有言论自由 的。而且只有诽谤漫骂具体个人的言论才可能被起诉，诽谤漫骂政府、某个组织、某种宗教或某个群体，都属于言论自由。<br />
<br />
8.言论自由不等于别人不能反对你的言论。除了对侵犯他人权利，给他人造成了实际损害的言论提起民事诉讼之外，别人还可以使用各种合法手段来反对或抗议你的言论，包括报纸拒绝刊登你的文章和网站禁止你发言。<br />
<br />
9. 言论自由总是存在边界的，即使是美国也要对存在“清楚、现实和迫在眉睫的危险”的言论加以限制。而且人们总是会对哪些言论不属于言论自由的范畴发生争议。 要从法律上对言论自由的边界加以规定，既可以通过国家立法来事前禁止，也可以通过法庭判例来事后确定，在此之外一切言论均拥有言论自由。但是国家的立法和 法庭的判例本身也有可能构成对言论自由的侵犯，例如中国刑法中的煽动颠覆国家政权罪等条款和北京市第一中级人民法院2009年12月25日对刘晓波先生的审判。</div>

							
							
							
							
