																											<div id="postmessage_506251" class="t_msgfont">老鼠所举横渡英吉利海峡的例子更贴近说明形式自由与实际自由，你有横渡海峡的自由，你却没有横渡海峡的能力，这是最常见的对西方自由民主的诟病，你给予劳动阶层以形式自由，他们却因经济条件和自身能力限制而无法从实际上享有这种自由。<br />
<br />
消极自由与积极自由都是某种状态，前者是处于不被干涉的状态，强调自我选择；后者是处于积极从事的状态，强调自我实现。积极自由虽然涉及自身条件和资源利用，但根本点是个人选择和主动从事，强调自主决定和自我主宰。我们回到英吉利海峡的例子。我的个人意志决定要横渡英吉利海峡，即使我没钱买机票，没有体力游过海峡，但我想各种办法利用自身资源实现横渡的愿望，比如锻炼身体、造木筏子，买橡皮艇，藏进运货船偷渡等等，由于这个过程完全是自主的，是个人意志使然而非他人强制或为了他人愿望的实现，因此即使我还没实现横渡，我依然处于积极自由状态中。<br />
<br />
由于积极自由中提到自身能力和资源利用，就给人以误解，让许多人把积极自由与实际自由相混淆了。</div>

							
							
							
							
