																											<div id="postmessage_366333" class="t_msgfont">文章提交者：老刺 加帖在 猫眼看人 <br />
昨天看了一个电影《狗镇》，感觉用震撼来形容都嫌不够，也许要用震惊才行。<br />
<br />
&nbsp; &nbsp;影片写一个黑社会老大的女儿格瑞丝（妮可・基德曼扮演）认为爸爸不正义，不想再做他的女儿，逃到偏僻深山中的狗镇。狗镇中的人们自私、麻木、残忍，把格瑞丝对穷人的浪漫想象打得粉碎。她的善良换来的是欺骗、冷酷、奴役、猥亵、**。她向往的天堂原来是地狱。最后她父亲来解救她时，她脖子上戴着枷锁，走路时用锁链拖着一个磨盘，因为躺在床上无法反抗，镇里所有的男人都**过她。她对父亲的权力从否定到肯定。当她父亲建议杀条狗吓吓镇上的人作为了断时，格瑞丝的建议是把全镇烧光杀光，并且冷血地亲手打死了欺骗她爱情的伪君子，没有一分钟的犹豫。<br />
<br />
&nbsp;&nbsp;令人感到震惊的是：影片明显的“政治不正确”，对穷人人性弱点的揭露毫不留情，有明显尼采哲学的意味。简直可以说完全是尼采哲学的电影版图解。但是，它绝不像我们那些政治思想图解的影片，只有骨头没有肉，它所有的人物、事件、思绪都真实可信，有血有肉，合情合理，无懈可击（尽管全剧没有用实景而完全是舞台布景拍摄，这点也很惊人）。因此，观影效果更为强烈。说白了就是一句话：你们都该死，你们不配活在这个世界上。它就能做到，让每个看完电影的人在黑帮血洗狗镇的时候有一种强烈的快感，欢呼雀跃，觉得解气之极。在片尾字幕时，还怕它的意思不够清楚，在背景中放了几十幅贫民窟的真实照片，肮脏、丑陋、血里糊拉。<br />
<br />
&nbsp;&nbsp;前些时看到一位生在农村的知识精英的文章，批评鲁迅对下层人的描写（如阿Q），说他把下层人写得愚昧、麻木，是利用手中的话语权欺负穷人，不正义。在他看来，只有讴歌穷人才是正义的。当时我对这文章就感觉有点似是而非。看了《狗镇》，才突然明白自己为什么会有这种感觉。<br />
<br />
&nbsp; &nbsp;贫穷是罪恶。早就听到过这种说法，原来很不理解，因为***的意识形态一直是表扬贫穷的。其实，贫穷有什么可表扬的呢？在几千年弱肉强食的历史中，贫穷不一直就是罪恶吗？贫穷就是在残酷的生存竞争中败下阵来。失败有什么可表扬的呢？贫穷不是美的，是丑的；不是值得骄傲的，是值得羞愧的。或者是比较懒惰，或者是比较笨，或者是不够幸运。如果一个人活成了下层人，那就是活得最失败的人，被淘汰的人，有什么可表扬的呢？<br />
<br />
&nbsp; &nbsp;中国解放后的三十年有点特殊：人为地设置了下层人向上流动的障碍，如工农业产品的剪刀差，如不许流动的户籍制度，如城乡双轨制，这就有点欺负穷人的意思，使农民对城里人有了怨气。使得上层显得不够正义，使得为下层说话有了点正义的味道。但是在人为障碍取消之后，这点正义也就慢慢消散了，我们又回到了尼采命题：贫穷到底是不是罪恶？<br />
<br />
&nbsp; &nbsp;总之，我觉得尼采和鲁迅如果没有走极端，没有说穷人就该死，那他们大体上还是站得住脚的，其理论有深刻的合理性，虽然显得有点“政治不正确”。</div>

							
							
							
							
