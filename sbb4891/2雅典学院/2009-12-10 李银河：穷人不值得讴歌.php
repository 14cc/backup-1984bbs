													<h2>《狗镇》再思</h2>
						
						
																											<div id="postmessage_366833" class="t_msgfont">(2009-04-03 18:40:20)标签：杂谈&nbsp; &nbsp;<br />
 <br />
&nbsp; &nbsp; 自从我写了《狗镇》的观后感，所有的朋友都跟我急了。李春光急赤白脸地问这是不是真是我写的，准备去辟谣；林春来信说，我相信你写这东西的时候不是你自己了。我也很震动，想了好几天。把我给李春光的回信照录在此：<br />
<br />
 <br />
<br />
李春光，你好。<br />
<br />
文章确实是我写的。是我看了电影后很震惊还没醒过闷来的时候写的，发在博客上，但是很快就删掉了。因为政治上肯定是不正确的。<br />
<br />
但是我觉得摩罗他们批鲁迅也有点问题，有点似是而非。其实读尼采时也并不很喜欢，觉得他太不同情弱者了。但是他的非理性完全一无是处吗？我很喜欢福柯，他也深受尼采影响，跟自由主义有点格格不入。我在这二者之间有点想不明白。应当说我只是把自己内心的疑惑提了出来，并没有定论。<br />
<br />
李银河<br />
<br />
 <br />
<br />
&nbsp; &nbsp; 仔细想了想与此相关的论题，看看问题出在哪里：<br />
<br />
&nbsp; &nbsp; 个人问题还是体制问题。一个社会有人穷有人富，到底是个人的错还是体制的错？我上次多说了个人的错，是有偏颇的。可是我那样说时心里还想到中国很多农村的地主，不止一个了解农村、了解这段历史的人说过，大多数中国的地主富农都是勤劳致富，恶霸只是极少数，而贫下中农倒是比较懒惰和不聪明的，所以中国的土改在很多情况下都是奖懒罚勤的（当然从政治上和把土地收归国有上是必须的）。有本书《半夜鸡不叫》就是给周扒皮翻这个案的。表扬贫穷没有道理。当然，这个个案不能证明贫穷都是个人努力不够造成的，纵观历史，横看当代，贫穷基本上应当说是体制造成的，是体制的不合理，不能怪个人。据说，中国现在的亿万富翁90%是高干子弟，这肯定是体制不合理造成的，绝不能说他们就该这么有钱，就是最优秀的人，最努力的人。<br />
<br />
&nbsp; &nbsp; 社会主义好还是资本主义好。从社会正义的角度讲，还是社会主义好。社会主义就是救济穷人，资本主义就是带来恶性的两极分化，富人愈富，穷人愈穷。所以像尼采哲学那样不同情穷人，不帮助穷人，是不正义的。社会主义就要大搞“杀富济贫”，当然是经济上的杀，不是肉体上的杀，就是要把从富人手里拿过来的钱为穷人搞福利，保障大家都有一个温饱体面的生活，不能“朱门酒肉臭，路有冻死骨”。中国的基尼系数从70年代的0.2飙升至0.5，从全世界最平等的社会成为贫富差异最大的社会，是一个大问题，搞不好要出大乱子。<br />
<br />
&nbsp; &nbsp; 要自由还是要平等。人生而不平等，有人长得美，有人长得丑；有人聪明，有人愚笨；有人生在富人家，有人生在穷人家。如果只强调自由和公平标准，由于起点不同，社会就可能失去平等和正义。在这一点上自由主义与尼采有相通之处，都强调自由，忽略平等。这也是我以自由主义的立场会掉进尼采陷阱的原因。尼采的哲学适用于丛林社会，弱肉强食，适者生存，只有公平，没有正义。就像《狗镇》里的社会，谁强谁就出头，谁弱谁就活该倒霉。如果这个世界永远是个丛林，那可真成了人间地狱了。<br />
<br />
&nbsp; &nbsp; 一位终身怀抱理想主义的朋友设想过一个乌托邦的社会，在其中所有的人都有一份基本的生活保障，其余愿意从事高级的具有创造性的工作的人可以仅凭自愿和兴趣去做，并不一定要高额报酬。因为他对做这样的事情有兴趣（比如写小说，画画，作曲，跳舞），你给不给报酬他都会乐在其中。我没有认真对待这个设想，觉得太不可能，太离谱。可是我不能不承认，这是一个非常有创意的、非常大胆的想法。虽然历史上没有成功的先例，但是可以成为激励我们改良社会的一个遥远目标。我觉得它很像马克思所设想的共产主义社会。当一个社会的财富积累到比较高的程度时，这未必不是一个社会改良的切实可行的方案。我强调社会财富积累这个硬指标是因为，贫穷的共产主义方案我们已经实验过了，失败了。</div>

							
							
							
							
