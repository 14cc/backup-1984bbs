在完成了上面冗长而枯燥的研讨后，似乎值得把各个重要结论归纳起来，对政治学来说，本书提供的资料是可以作为根据的。<br />
<br />
合众国的宪法运动主要由四个《邦联条款》下受到侵害的动产集团发起和推动的。这四个集团是：货币、公债、制造业、贸易和航运。<br />
<br />
制宪的第一个稳健的步骤是一个小而积极的集团完成的，他们通过自己的私人财产从自己的努力结果中获得了直接的利益。<br />
<br />
关于召开制宪会议的提议，并未经过直接或间接的人民表决。<br />
<br />
