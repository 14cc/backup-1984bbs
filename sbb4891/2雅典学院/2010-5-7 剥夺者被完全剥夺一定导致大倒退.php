<br />
起草宪法的费城会议的成员，除少数外，都从新制度的建立上获得了直接的私人利益。<br />
<br />
宪法在基本上是一部经济文件，它的基本观念是：基本的私人财产权先于政府而存在，在道德上不受人民多数的干涉。<br />
<br />
据记载，制宪会议的大部分成员都承认财产权在宪法上应有特殊的巩固的地位。<br />
<br />
在批准宪法方面，约有四分之三的成年男子未能参与对于这个问题的表决；他们或者由于漠不关心，或者由于财产限制而被剥夺了公民权，而没有参加选举出席州代表会议的代表。<br />
<br />
宪法的批准，大约只有不到六分之一的成年男子投票赞成。<br />
<br />
在纽约、马萨诸塞、新罕布什尔、弗吉尼亚和南卡罗来纳等州，参加州代表会议代表选举的多数选民是否赞成批准宪法是有疑问的。<br />
<br />
在各州批准会议上拥护宪法的领袖们，所代表的经济利益与费城会议成员所代表的经济利益完全相同；其中的多数人也都从他们努力的结果上获得了直接的私人利益。<br />
<br />
在批准问题上，事实表明赞成和反对宪法两派之间有一条鸿沟，一边是殷实的动产利益集团，另一边是小农和债务人集团。<br />
<br />
