													<h2>于建嵘：反社会人格，最恐怖的发泄</h2>
						
						
																											<div id="postmessage_594164" class="t_msgfont">于建嵘：反社会人格，最恐怖的发泄<br />
 来源：南风窗 作者：于建嵘 2010年04月29日15:10我来说两句(23)复制链接大中小大中小大中小3月23日7时许，福建省南平市实验小学门口，前外科医生郑民生55秒内连续刺杀13名小学生，造成8死5伤的惨痛后果。郑42岁，失业、无房、无妻无子。据其自供的作案原因：一是辞职后工作无着，二是恋爱失败，三是受一些身边人员闲言刺激。这些并非很特别的原因，却引发了震惊全国的南平血案。4月8日，一审当庭宣判郑民生故意杀人罪成立，依法判处死刑。<br />
<br />
这是近年来发生的又一起十分严重的个体极端事件。但这起案件既不同于邓玉娇式的“自卫性抗争”、唐福珍式的“自残性维权”，也与马加爵、杨佳式的“报复性攻击”有明显差别。笔者将它称为“泄愤性暴力”。它的主要特征是，行凶者心中对社会有诸多不满，但却没有“合理”的明确仇恨；他泄愤的目标不是侵犯他的人，也不是公权力，而是更柔弱者，如郑案中缺乏反抗能力的小学生；作为绝望的“失败者”，残杀无辜报复社会成了自我实现的手段，为此值得付出生命的代价。南平血案的严重性其实并不在于受害者是小学生，也不在于伤亡人数的多少，最关键的是攻击目标具有放大性，使每个无辜者都有沦为暴力受害者的可能，因此是一个兼具动向性和标志性的事件。 <br />
<br />
南平血案的第二天，笔者正好到了福州，在和当地党政干部的交谈中，感觉这种类型的暴力由于其攻击目标的不特定性，给当地带来了一片恐惧。这很容易理解。社会问题增多，社会群体的分裂带来的冷漠和隔阂，正在造就大批潜在的郑民生，不知道哪一个哪一天会突然爆发，每个人理论上都可能成为这种“无差别杀人”的受害者。不论是非对错，不分善恶曲直，只是个概率和运气的问题（尽管可能性很小）。<br />
<br />
而郑案的传言之一，即特意打摩的到较远的当地“贵族小学”南平实验小学作案，更是加深了生活较优越阶层的恐惧，甚至对弱势群体产生总体性的排斥和对立，而这只能造就恶性循环。如果官民、富穷之间的分裂加深，产生“我们―他们”之间的对立，社会会向更危险的方向发展。要消除这种恐惧，我们不得不问，南平血案发生的原因何在？郑民生是如何从“郑一刀”变为“郑屠刀”的？<br />
<br />
让潜在的郑民生们不再恐惧<br />
<br />
实际上，这种因个人原因滥杀无辜（弱）者的极端案例（区别于非为政治、宗教目的的恐怖活动），在不少国家都有发生，比如美国有赵承熙式的校园枪击案，日本有宅见的池田小学杀人案、加腾智大的秋叶原无差别杀人，香港也发生过异曲同工的天水围伦常惨案，罪犯都是各自社会中的边缘人物，性格孤僻、生活失败、缺少安慰、怯懦厌世。这些共同点表明，绝望的“失败者”通过残杀无辜来报复社会，首先与人性有关，是一种人类社会现象。<br />
<br />
毕竟，再坏的社会制度、再多的社会问题、再被人蔑视欺负侮辱，最多逼你去反抗压迫者，而不是让你去滥杀无辜。换个角度说，屡遭打击没有出路的人也很多，去残杀无辜的却是个例。从心理学的角度来说，这类行凶者可用反社会人格障碍来解释。具体到郑民生，有心理学家分析其社会适应不良（与领导同事同学哥哥关系都不好）、行为无计划性（轻率辞职、杀人无预兆等）、自我认知存在问题、易迁怒于人（比如将辞职说成领导诱使）、缺少羞愧感（杀人后无悔意）等，这都是其制造血案的个体原因。外因是变化的条件，内因才是变化的根据。因此，笔者认同行凶者的主观因素是这类惨案的主要原因和直接原因。对南平血案的讨论是在这个前提下进行。<br />
<br />
“杀人狂”的产生虽然很难从制度层面杜绝，但是，这不代表不需要对社会进行反思，毕竟，并不是每个失败者或反社会人格障碍者都会变成现实中的杀人罪犯。所以必须要关注的是，社会对其“失败”是否要承担一定的责任，又有多大责任？发生惨案的各国事后对此均有讨论，答案也各不相同。具体到郑民生案，笔者认为至少存在以下几个方面的问题。<br />
<br />
第一，无房是郑民生的“软肋”之一，但这并不是其个人懒惰造成，社会应负有很大的责任。作为一个具备专业技能、长期供职于正规医院的医生，他凭借工资收入却买不起一套房，甚至没有希望，这在大多数国家是难以想象的，不能不说我们的社会利益分配体制或者经济结构存在失衡。另外，据说郑民生因底子薄错过单位改制入股，从而失去了分红的机会。但是，如果再努力工作，工资收入也远不能与一次“投资”的收益相比，这会多么让人灰心丧气。社会做不到宣传的“以按劳分配为主”，是否说明政府在社会转型期的宏观政策存在一定问题，并且缺乏日后的补救措施？<br />
<br />
第二，社会失范，遵守规则者却常被边缘化；价值观单一，无钱无权者得不到尊重，是郑民生人生悲剧的催化剂。郑民生是个医术、医德都不错的医生，却不善于搞人际关系，应该是希望凭技术吃饭。但不屑或不会搞潜规则、拉关系，不仅没有灰色收入，升职也难，最后因为“不能适应社会”而被人瞧不起。循规蹈矩并没有给郑民生带来什么好处，反而在事实上过得比同事们差（虽然并没有证据指责同事们玩“猫腻”）。另外据说郑在辞职前已联系了新单位，但却在入职时被告之要降职降薪，“强者”不守规矩，正是我们社会的问题之一。社会缺少公平正义的环境，不能让守规则者得利，反而是不守规则者得利，对社会的失望，有可能带来郑民生对整个社会的反抗。<br />
<br />
第三，社会地位的变化，政府对失业者缺少关怀，社会对弱者缺少同情，是郑民生走向消沉、绝望、愤怒的原因之一。郑民生之前在中专最红火的时代考上中专，顺利进入体制内并成为业务能手，在贫困的家庭中成了顶梁柱，在邻里当中可算是个“精英”。但是，辞职并未去成联系好的医院后，外科主治医师的职称并不能保证郑找到工作，开个人诊所也未通过卫生局审批。起码在新闻报道中，我们没有看到政府民政部门对他有何救济和帮助，社会对他有何关心，曾经的“郑一刀”，就这样被社会抛弃了。郑民生从前风光过、后来被甩了出来这种遭遇，更强化了他的失落和愤怒。<br />
<br />
总之，“业务上的认真和负责”不能解决起码的住房问题，强者不守规矩不用付出成本和代价，失业得不到社会保障和救助，郑民生的生活从充满了不确定性，到渐渐失去希望。社会制度的基本规则难以依赖、不可信任，生活在其中的人们没有办法预料未来，产生了挥之不去的恐惧。郑民生由不确定产生恐惧，恐惧演化为仇恨，仇恨需要发泄，最终变成了罪恶。郑民生不但是他人恐惧的制造者，自己也是恐惧的受害者。从这个角度说，上述社会问题正是郑走向犯罪的外因，充满了社会转型期的独特色彩。因此，要消弭南平血案引发的社会恐慌，首先就要让潜在的郑民生们不再恐惧。<br />
<br />
“示范效应”<br />
<br />
而南平血案的可怕并不仅局限于其本身，更可怕的是已经产生了一定的“示范效应”。 3月26日上午，在南平市第一人民医院的重症监护室里，一名中年妇女突然拦住慰问受伤孩童的南平市委书记雷春美，并口出惊人之语: “我女儿的冤屈得不到伸张，如果你们不处理好，我也去杀人！” 3月30日，在举报邻居违建半年无果后，武汉的刘先生在网上留言：“如果我的合法权利一直得不到保护，我只有效仿郑医生的做法……”也许这只是种威胁， 但再联想到前不久暨南大学女大学生无辜被扔下11楼，天津司机恶意撞人案等，就说明社会问题的恶化和矛盾冲突的增多，大量弱势群体和“失败者”的存在，已经给这种由生活不确定性演变而来的滥杀罪恶，提供了潜在的土壤。戾气和暴力若传染开来，可能就不是“反社会人格者”的极端个例那么简单了。<br />
<br />
喊冤的母亲是为8岁女儿被人强奸罪犯未能伏法，刘先生是为城市违建难以拆除，这两个并不一定具备很强代表性的案例，却足以说明我们社会中“弱者”的产生与公权力过强有关。而更为常见的情况，比如因征地产生的无地农民，血拆造就的自焚自残者，因上访而被地方政府非法拘禁、劳教、送精神病院者，更加深刻地说明，面对不受约束的权力，每个人都有可能成为社会的弱者，即使今天他看起来是所谓的强者。唐福珍是个并不贫穷的女企业家，上访的队伍中还有曾经的信访办主任。在一个政府权力与公民权利界限不清、公权力缺乏有效监督和制约的社会，在一个讲法制而无法治、先讲政治再讲法律的社会，在一个民众无法有效组织共同表达、共同争取利益的社会，弱者不仅仅是我们日常所说的经济上的贫困者，也不仅仅是社会的边缘化群体，而是一个相对的概念。<br />
<br />
面对失范的公权力，每一个个体都是弱者，面对社会规则不确定的后果，心中都充满了恐惧。为了克服这种恐惧，让自己的未来更具确定性，有些人会更加追求权力和财富，力争成为制定规则、利用规则的强者，而做不到这一点的人则会用其它方法来排解自己的焦虑，无法克服恐惧的绝望者则会成为潜在的郑民生，受不可知的因素的刺激成为社会的破坏者。而对待恐惧的这两种方式，又会造成强者掠夺弱者、弱者用暴力反抗强者，两者形成恶性循环。绝望者越多，对社会的破坏性就越强，最后造成人人自危的境地。<br />
<br />
南平小学生事后写的作文中，就有为何不去杀贪官的疑问。社会舆论暴露的已经失范的社会心态，很值得人担忧。这可能会加深社会上强势阶层与弱势阶层的互相恐惧、互相防范，带来进一步分裂。实际上，历史已经无数次告诉我们，以暴制暴，只能带来社会的动荡，生灵的涂炭，最终是社会重新洗牌，又一个轮回。对现代社会来说，这是代价惨重的，也是前途堪忧的。社会要想走向公正、和谐、文明，更好的办法是通过理性和平的道路。因此，我们现在就要对滥杀的暴力事件加以最大的警惕，尽量防患于未然。<br />
<br />
降低不确定性带来的恐惧<br />
<br />
要尽量减少类似事件的发生，笔者建议从以下几个方面入手。<br />
<br />
首先应调整社会分配体制，解决好民生问题。让一般人通过正常劳动可以买得起房，起码有买得起的希望。要做到这一点，一是要切实保障个人的基本权益，建立各种利益的表达机制；二是要约束强势集团的掠夺行为，包括热衷卖地的地方政府和谋取暴利的房地产商；三是要加快各类社会保障性住房建设。另外，还需要加强对社会边缘人的物质救济和精神帮助，使他们能有尊严、有希望地生活下去。<br />
<br />
其次，应采取各种办法，使社会各阶层之间更容易流动、更有规则地流动。只有合理化流动的加强，才能消解官民之间、贫富之间、强弱之间的“我们―他们”式的结构对立。尤其需要保障底层知识分子的基本物质权益。中国数千年来的历史进程表明，他们往往具有相当的号召力、影响力和破坏力，这一群体的上升通道尤其需要重点关注。<br />
<br />
再次，应加强心理干预。南平惨案的背后，一方面需要挖掘背后的社会问题，另一方面还需要剖析郑民生个人的人格、心理问题。建立社会心理发现和干预渠道已到了迫在眉睫的地步，对弱势群体的关怀和心理引导必须进行重点强调。可以对重点人群提供免费或低价的心理治疗。还应该加强社区建设，使弱者能找到归属感。社区是社会的最基层单位，加强社区为代表的基层社会建设，不仅让它成为我们生活的家园，更要成为心灵的家园。通过社区工作者建立一种心理疏导的机制，能及时发现问题并对社会情绪进行疏导，对社会心态进行引导，对一些反社会行为进行干预。使从源头解决问题成为可能。<br />
<br />
除此之外，更重要的是约束公权力。正常的社会也不能完全避免反社会的“杀人狂”，但不正常的社会，会逼得更多正常人走极端。要想真正限制公权力过强，笔者认为应做到四点，即明晰的产权、有权威的司法体系、真正的代议制度和开放的媒体，而这在现行宪法框架下完全可以做到。应该让每个公民的努力和付出都能有适当的回报，让遵守规则者得利；让每个公民都知道宪法是社会的底线，从而可以合理地预判自己行为的后果；让公民通过结社等争取共同利益；在公民自由表达的基础上建立多元价值观。这样才能降低民众因不确定性而产生的恐惧，避免南平血案式的恶性事件。<br />
<br />
总之，南平血案的发生及其所暴露扭曲的社会心态，呈现出病态性的社会心理疾病，也意味着中国进入了一个社会心态更复杂的阶段。如果不及时调整和积极处理，必然会影响到和谐社会进程，甚至发生一些大家都不愿意看到的事情。而要让弱者免于生活无着的恐惧、免于被欺辱蔑视的恐惧、免于被社会抛弃的恐惧，让普通人免于成为无辜受害者的恐惧，需要社会各方面的努力。<br />
<br />
（作者为中国社会科学院农村所社会问题研究中心主任、教授）</div>

							
							
							
							
