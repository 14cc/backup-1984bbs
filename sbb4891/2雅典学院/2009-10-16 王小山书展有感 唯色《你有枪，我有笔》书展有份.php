<br />
从柏林墙到防火墙（GFW）<br />
<br />
 <br />
<br />
（德国《日报》网络版稿件，其他媒体请勿转载――中国大陆媒体请随便转载，不收稿费）<br />
<br />
 <br />
<br />
2006年，我曾经在柏林住过3天，3天时间里，基本只去了一个地方，那就是柏林墙。我和另外一个朋友沿着柏林墙遗迹不停地走，走过大街，跨过河流，有心的柏林人给了我们很大方便，原柏林墙沿线都留有足可辨认的痕迹――不过，还是推得太干净了，只保留了很小的一段。我很喜欢保留那段墙体上各国艺术家的涂鸦，但还是想，多保留一些，会更好，它能让我们的子孙知道，柏林人曾经过的什么日子。<br />
<br />
 <br />
<br />
毫不意外，那年回到中国后，最受朋友们欢迎的礼物是价格从3.5欧元到6.5欧元不等的柏林墙碎片。<br />
<br />
 <br />
<br />
柏林墙倒了，到今年整整20年，纪念日也很快到来，相信柏林人已经想好了如何庆祝这个日子。<br />
<br />
 <br />
<br />
无论如何，我乐于看到柏林墙的倒掉，这有我的一份贡献――20年前的5月份，我在北京的一所大学里读书，和很多同学一起，在天安门广场绝食。到现在，我依然觉得，如果没有北京学生和市民的抗争，柏林墙也终究会倒掉的，但速度至少会慢一些。我没想让柏林人感谢我，但就是高兴。<br />
<br />
 <br />
<br />
昨天（2009年10月15日），记者无国界组织在法兰克福书展召开新闻发布会，提到了很多中国问题，比如“截访”（jie fang，很难翻译，大致意思是很多受了冤枉的人，到北京上访，而他所在的地区政府会派专人阻截他，免得他到了北京，惹出祸来，影响“稳定”，最终影响当地官员的仕途），比如擦边球（ca bian qiu，指一些勇敢的编辑记者发表有可能令政府不快的稿件，以试探政府能承受的底线，我的同事中，就有过线被投入监狱的先例），也提到了另外一堵墙，那就是GFW（GREAT FIREWALL）。它不是人们熟知的GREAT WALL，而是一种网络技术，利用软件阻隔中国国内的互联网用户登录政府不喜欢的网站。Great wall有两千多年的历史，建立的目的是为了防止北方民族的入侵，而GREAT FIREWALL的历史不超过十年，关闭的是中国人了解世界的窗口（2008奥运会期间，GFW曾经短暂而有限地开放）。从GREAT WALL到GREAT FIREWALL，中国走回了历史。<br />
<br />
 <br />
<br />
1963年6月23日，美国人肯尼迪站在柏林墙边说：“自由有许多困难，民主亦非完美，然而我们从未建造一堵墙把我们的人民关在里面，不准他们离开我们……一切自由人，不论他们住在何方，皆是柏林市民，所以作为一个自由人，我为Ich bin ein Berliner这句话感到自豪。”<br />
<br />
<br />
我对任何政治人物的承诺、激情都抱有怀疑态度，无论是中国的还是美国的，但肯尼迪“Ich bin ein Berliner”的说法还是足够打动我――可惜的是，面对着比柏林墙更坚固的GFW，目前为止，我还没听到任何一个其他国家的人说出“我是中国人”。欧洲的总统总理首相们更热衷的是和中国做生意，赚取钞票；德国许多作家、汉学家，则忙着和中国官方代表团的御用文人插科打诨；欧洲很多普通人看到的是中国商品的低价格“优势”抢了他们的饭碗，从马德里到莫斯科，莫不如此――在这一博弈过程中，更能体现出肯尼迪“自由是不可分割的，只要一人被奴役，所有的人都不自由”的涵义，中国的产品很多来自“血汗工厂”，那里的工人缺乏基本保障，工资低到欧洲人难以想像，生产出来的产品自然价格低廉，这更像是巧妙的报复：你不关注他们的人权状况，他们就会让你失业。<br />
<br />
 <br />
<br />
墙，悲观地说，人人之间都有一堵墙，语言、教育程度、种族、国籍等等，都是建造隔离墙的材料，但主动造墙隔断人民之间的交流，曾经的德国人和现在中国人做得最为极致，区别不过是德国人用的是石头，中国人用的是数字而已。</div>

							
							
							
							
