													<h2>王小山书展有感  唯色《你有枪，我有笔》书展有份</h2>
						
						
																											<div id="postmessage_285073" class="t_msgfont">唯色：我的德文书《Ihr habt die Gewehre, ich einen Stift》在法兰克福书展上<br />
<br />
2009年3月10日，关于2008年西藏事件大事记――《鼠年雪狮吼》，由台湾允晨文化出版。同月，该书藏文版在印度达兰萨拉出版，承尊者达赖喇嘛和首席噶伦桑东仁波切题词。<br />
<br />
最近，《鼠年雪狮吼》翻译成德文，由德国Lungta Verlag 出版社出版，书名《Ihr habt die Gewehre, ich einen Stift》（意为：你有枪，我有笔），译者 G.G.。<br />
<br />
在昨天开幕的第61届法兰克福书展上，我的第一本译成德文的书，出现在书展上。在这里，我向译者G.G.和Lungta Verlag 出版社致以衷心的感谢。<br />
<br />
我在台湾出版的多本中文书籍，也参加了这次书展，由衷地致谢台湾大块文化和允晨文化。<br />
<br />
<strong>德国公共广播电台（ARD）近日采访我谈法兰克福书展</strong><br />
<br />
<a href="http://www.hr-online.de/website/specials/buchmesse2009/index.jsp?rubrik=47092&amp;key=standard_document_38152847" target="_blank">http://www.hr-online.de/website/ ... d_document_38152847</a><br />
<br />
中国是本届法兰克福书展的主宾国。中国方面特地翻译了117部书籍参展，其中没有唯色的作品。唯色是一位藏族女作家。她的作品由于对中国政府的批评态度而被禁，网页遭到封锁，而本人也受到严密的监视。她得不到护照，因而也不能亲自参加法兰克福书展。但她通过台湾出版社出版的书籍还是会出现在书展上。<br />
<br />
跟作家唯色女士的碰头地点选择在北京市中心的一家咖啡店里。北京秋天气温还很高，咖啡店里的空调机发出嗡嗡的声音。唯色很难在自己的家里接受访谈。她的电话受到监听，行动受到监视，因为她在中国政府的眼里是个危险分子。唯色是藏人，她的书籍和文章都是通过网络发表的。她的作品描述了文化大革命对藏人文化的破坏，揭露了藏人在经济和职业方面受到的不公正待遇。唯色的作品具有批评性，冲击了禁区。她称，她从写第一本书开始，以后所有作品都遭到禁止：&quot;我没有想到我这本书会惹出这么大的麻烦来。从中国政府的角度来讲我犯了很严重的政治错误。因为我没有把达赖喇嘛看成是分裂分子，他们让我做自我批评。但我说，我是佛教徒，又不是共产党员。&quot;<br />
<br />
唯色因此丢掉了编辑部的工作。为了防止唯色离开中国，她得不到出国的护照。尽管如此，唯色并没有被中国的安全部门吓倒。唯色的体恤衫上印着西藏英文写法&quot;Tibet&quot;的字样及布达拉宫的图案，甚至还有达赖喇嘛的肖像。而达赖喇嘛的肖像在中国也受禁止。尽管失掉工作，唯色仍坚持写作。她最新的一本书有关去年拉萨骚乱事件。唯色要求政府多做解释工作，少些压制措施：&quot;西藏事件已经发生一年多了，而中国政府并没有丝毫改变其立场。西藏还是有许多军警。说是没有军事管制，实际上还是采取军事管制的办法。<br />
<br />
批评政府的文章与书籍在中国是没有机会发表的。唯色说：&quot;因为我的书在中国出不了，我的书都是在台湾和香港出的。我的书写完了就通过电子信发过去。如果图片多的话我就制成光盘托人带过去。&quot;<br />
<br />
唯色找到了躲避监控的方法。在过去的几年中，不少持不同政见者遭到逮捕或者受到恐吓。唯色称，如果有一天国安部的人站在她门口的话，她不会感到意外：&quot;这个国家在政治上的专制是很严重的，甚至超过了前几年。对持不同政见者的打压很残酷。有不同意见的人生活在这个国家中如履薄冰，很紧张。&quot;<br />
<br />
今年四十出头的唯色由于丈夫王力雄的原因选择了北京作为她的流亡地。她丈夫也是作家，作品在中国也受到禁止。但这对作家夫妇仍旧坚持写作。唯色表示，他们没什么太多可失去的了：&quot;不让我们出书对我们作家来说是个很大的惩罚。我的书在西藏很受欢迎，但人们在西藏买不着。只能冒很大风险从外面带。这对于一个作家来说就受到很大的限制。人们在中国难以听到我的声音。&quot;（译者:ard/王雪丁）<br />
