<br />
这是我第三次到法兰克福，第一次是2004年，采访中国到欧洲首游团，在法兰克福-巴黎-布鲁塞尔-阿姆斯特丹-卢森堡之间转了一个小圈子；第二次是2006年世界杯，作为足球记者，游历了德国至少12个城市；但前两次都没见到这么多同胞――机场里中国人排了最长的队，火车站下面SB等车时，两个中国人隔着铁轨大声讨论着什么，我仿佛置身北京地铁某站；酒店里吃早餐的人里，也有一半来自中国，闲聊起来，都是奔着书展来的。<br />
<br />
中国融入世界，已是大势所趋，即使毛泽东从他的水晶棺材里爬出来，也无能为力，最多叹一口气，再躺回去。中国人的脚步慢慢丈量着世界的土地，中国政府也在海外到处争取露脸的机会，法兰克福书展当然是这种“争取”计划的一部分。我见过一些报道，说至少一部分德国媒体和德国人，对书展的举办方纵容中国政府很不感冒，我当然也赞同这样的观点。书展举办方，应该不屈从任何压力，无论是商业的还是政治的，这毫无疑问。但仅从书展的举办说，这样的展览越多，实际上对中国的发展越有利，对中国有利（当然不是指对中国政府有利）的事做得越多，对整个世界都有好处――无法想像一个拥有至少13亿人口的国家过分封闭，一旦“爆炸”，会给世界带来多大损失，而这种可能性，谁也不敢保证已经消除。<br />
<br />
之前，在法兰克福有过一次研讨会，参加者有戴晴、贝岭、秦晖、徐星等人以及中国政府代表团。我不知道德国人是否理解戴晴和贝岭发言时，中国政府代表团集体退场的举动――中国政府有一句口号：稳定压倒一切。稳定的意思就是不出事，是安全。去年奥运会之前，火炬在国内100多个城市传递，为了确保绝对安全，有些城市甚至不让市民参与――假使火炬被抢了吧，相关负责人一定会下课（被撤职）。说回当天的中国政府代表团，他们在戴晴和贝岭发言时集体退出，看上去很没风度很没必要很没意思，但却是他们处境下的最佳选择了，至少他们回去不会被撤职，他们心里怎么想的不重要，但一定要让党看到，他们的屁股是和党坐在一起的。在中国现在，依然会有这样的事情――A借朋友B的手机打电话，说上几句，B大惊失色：原来你说这么敏感的话题，别用我的手机了，借别人的吧。―― 我们依然生活在恐惧中。<br />
<br />
戴晴和贝岭，一向是作为中国政府的反对派出现的，秦晖呢，虽然写过很多很好的文章――好文章，意味着不讨政府喜欢――但他是清华大学的教授，算体制内人士，中国政府代表团的人里会有人和他打打招呼，说上句话，最惨的是我的朋友徐星，20多年没正式工作，跟体制毫无瓜葛，到了法兰克福，中国政府代表团硬是没一个人跟他说任何一句话，连个招呼都不打，他只能一个人呆呆地发愣，到现在为止也不知道为什么。他可能更不明白的是，为什么到正式书展的时候，德方根本不邀请他了，听说意大利人倒是跟他商量过，要请他来。这多可笑：一个中国作家来参加一个以中国为主宾国的书展，要由第三国出面邀请。<br />
<br />
我也不知道，究竟为什么呢？――大家一起装傻呗。</div>

							
							
							
							
