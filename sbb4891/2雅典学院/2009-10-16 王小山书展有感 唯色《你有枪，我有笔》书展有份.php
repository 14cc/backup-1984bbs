																											<div id="postmessage_286807" class="t_msgfont">法兰克福书展观感之二：莫言，与中间地带<br />
王小山 @ 2009-10-16 0:04 阅读(1661) 推荐值(1324) 分类: 百城记<br />
<br />
莫言，与中间地带<br />
<br />
 <br />
<br />
（德国《日报》网络版文章，其他媒体请勿转载。 ）<br />
<br />
莫言是中国当代最重要的作家之一，他的中篇小说《透明的红萝卜》，在我20多年前读大学的时候就是老师重点分析的作品，而他最优秀的长篇小说《丰乳肥臀》更是通过对一个家族几十年生活的描述，成为人们了解中国现代史的最佳途径之一。<br />
<br />
 <br />
<br />
很多德国读者知道中国，是通过张艺谋的电影《红高粱》，而这部电影就是根据莫言的小说拍摄而成的。电影《红高粱》在中国曾经是一个话题，话题的发起者认为，上个世纪80年代的张艺谋通过描写中国人贫穷落花的一面取悦西方读者以及电影节的评委，从而获得金熊奖建立自己的名声，是中国的“罪人”。而我觉得，《红高粱》中的角色感情炽烈、有血有肉，敢作敢为，体现的是中国人好的一面。――这些争论现在看已经没有意义了，张艺谋已经成了中国官方御用的导演，专门为官方涂脂抹粉，歌功颂德的艺术家，其地位类似纳粹电影《意志的胜利》和《奥林匹亚》女导演Leni Riefenstahl。但不可否认的是，张艺谋曾经是一名优秀的艺术家，善于从小说家的作品里获得灵感，他的第一次成功，依靠的是莫言。<br />
<br />
 <br />
<br />
日本小说家大江健三郎在诺贝尔奖的答谢词里，提到的中国作家就是莫言。<br />
<br />
 <br />
<br />
如果明年诺贝尔文学奖颁给莫言的话，是一件皆大欢喜的事，他现在不但得到中国文学爱好者的喜爱，同时也获得了官方的认可，他成为法兰克福书展开幕式上中国作家的发言代表，就是明证。<br />
<br />
 <br />
<br />
按照计划，14日上午11点，书展会有一个莫言作品朗诵的活动，但最后不知道什么原因，这个活动取消了。计划里，同时举办的活动，与中国有关的，至少还有两项，一个是“孔子与歌德对话”，一个是戴晴、马健等流亡作家举办的控诉中国政府压制言论自由的活动。<br />
<br />
 <br />
<br />
可以想像，来法兰克福的100多个中国官方作家，没有一个会参加最后一项活动――事实也是如此，那里大部分都是其他国家的记者和五六个中国留学生。<br />
<br />
 <br />
<br />
在法兰克福书展，能见到两种不同的中国人，一部分是包括莫言在内的中国官方代表团，一部分是以马健杨炼为代表的流亡作家，这两种人就像阿尔卑斯和喜马拉雅，都是山，但永远没有见面的机会，尤其是前者，一定会刻意回避后者的存在，小心翼翼地闪躲，即使见到也视若无物。这两种人，前者完全由纳税人人供养，由政府管理的作家协会操控，而后者，也有一部分由纳税人供养――他们在监狱里。<br />
<br />
 <br />
<br />
像莫言这样的作家，我的理解，他热爱的只是文学，并不操心，或者不表现出来对言论自由的关注，也是出于一种无奈的选择，毕竟，不是人人都忍受得了经常被警察骚扰以至于有进监狱可能的生活，而他除了不得不出席官方的活动，成为政权一部分外，其他表现，离张艺谋差得很远――我称之为中间地带，也就是离两个极端都有距离的那部分人。我理解一个作家，只是为了文学而不得不尽量能让自己过上平静生活的愿望，谁也无权要求每个人都成为战士，毕竟，他不是王兆山，这已经足够。<br />
<br />
 <br />
<br />
王兆山是一个极端的典型，2008年，他写过一首词，全文如下：天灾难避死何诉，主席唤，总理呼，党疼国爱，声声入废墟。十三亿人共一哭，纵做鬼，也幸福。银鹰战车救雏犊，左军叔，右警姑，民族大爱，亲历死也足。只盼坟前有屏幕，看奥运，同欢呼。<br />
<br />
 <br />
<br />
这是典型的为官方拍马屁的作品，在中文网络上掀起轩然大波，王兆山被称为“王羡鬼”，甚至，去年新任中国作家协会主席的铁凝对此也极为不满――吊诡的是，此次法兰克福书展中国作家代表团中，依然有王兆山的大名。这至少告诉我们一个道理，中国官方根本不在乎一个人是不是很恶心，只在乎你是不是听话，为政府代言做宣传。王兆山的正式身份是山东省作协副主席，即使他的词让很多中国人不爽，但并不妨碍他继续作官，不妨碍他成为中国作家的代表――而中国作家代表团其他100多人，也没表现出来因和他同列代表而羞耻――其实，这些人也没有办法，有意见，也无从表达，最多私下里发发牢骚而已。<br />
<br />
 <br />
<br />
以我的了解，莫言固然不会成为马健，不会成为杨炼，不会成为戴晴，站在与中国政府对立的立场说话，但他也绝不会成为王兆山，写出让人恶心到吐的诗词来。<br />
<br />
 <br />
<br />
13日下午，见到莫言时，我问，晚上开幕式，你要讲话是吧？莫言开玩笑说，他们写了稿子，我就讲吧。开幕式上，莫言果然讲了，但我不觉得那稿子完全是别人的手笔，至少有一些经验是他本人的。莫言讲了这样一个故事，100多年前，德国人占领山东时，中国人传说，德国人没有膝盖，只要用棍子捅倒，德国人就再也爬不起来，另外，德国人的舌头也和中国人不同，是分叉的。10几年前，有德国朋友到他山东老家访问，莫言的爷爷还悄悄把他拉到一边问：德国人原来有膝盖啊，而且舌头也没有分叉。莫言把爷爷的话转述给德国朋友，德国朋友哭笑不得。莫言要说的是交流的重要，因为100多年前，欧洲也有传说，说中国人住在树上，脸颊似鸟。<br />
<br />
 <br />
<br />
是啊，100多年过去了，中国人和西方已经从相互妖魔化变成了乐于交流――至少在这一点上，人类是大大进步了的。无论是莫言还是默克尔习近平，都在强调交流的重要，莫言强调的是相互交流消除彼此的陌生感，习近平强调的是意识形态的不同不能成为交流的障碍，默克尔强调的是愿意提供法兰克福这样适合交流的平台――仅仅从这一角度出发，中国成为法兰克福书展的主宾国，是大大的好事。<br />
<br />
 <br />
<br />
建议想了解中国的德国读者，不妨从莫言的作品开始，他才华横溢而不极端，相对温和――如果只看王兆山的作品，你会觉得中国一片和谐之声，如果只听反对派的声音，会觉得中国一片黑暗。我想说的是：即使中国有很多作家，包括我的老师刘晓波先生，被投入到监狱里，即使我这样并不极端的作家，经常被警察骚扰，也要承认中国的进步，因为再早几十年，这些人面临的是直接被枪决的危险，而不是尽管装模作样的法律程序；但话说回来，尽管中国有很大进步，离真正的言论自由民主等等普世价值（莫言的演讲中提到了这四个字）的实现，也还有很长的距离。</div>

							
							
							
							
