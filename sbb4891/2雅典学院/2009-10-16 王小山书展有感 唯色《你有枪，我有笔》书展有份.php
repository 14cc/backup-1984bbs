																											<div id="postmessage_286808" class="t_msgfont">法兰克福书展观感之三――余华，或者别人<br />
王小山 @ 2009-10-16 0:08 阅读(1494) 推荐值(171) 分类: 未归类<br />
<br />
余华，或者别人<br />
<br />
 <br />
<br />
（德国《日报》网络版作品，其他媒体请勿转载。）<br />
<br />
 <br />
<br />
歌德大学，余华在和100个左右的听众对话，这个当年的牙医完全脱胎换骨了，语言便当，风趣幽默，如果不当作家，也可以做一个到处演讲赚取外快的人。当然，如果不是作家的话，也没人愿意听他的话了。――我知道，这是废话，但我对余华有特别的感情，有一年时间，我和另外一个朋友，在北京租房，天冷，没办法，我们读诗歌和余华的小说，取暖。<br />
<br />
 <br />
<br />
余华的小说，以残酷著称，他对自己的评价是另外两个字，“强烈”，什么强烈？他没说，我的感觉是，黑暗势力的强烈，他的小说大半以悲剧结尾，甚至有展览苦难博取同情之嫌。但他自己的说法更像是一个玩笑，歌德大学教室里贴了很多中国作家的照片，只有余华照片的背景完全是红色，他称这个为“强烈”，而不具体说明究竟是什么“强烈”。<br />
<br />
 <br />
<br />
在红色中国，一个以描述黑暗，或者说悲惨世界的作家走红，本身就是一个奇迹，任何人都不能无视中国的进步，比如说，早上30年，余华也是不可以在中国存在的。而现在，中国政府，最多是封杀他的电影。<br />
<br />
 <br />
<br />
有读者问余华，对他做编剧，张艺谋导演的电影《活着》在中国大陆无法上演怎么看，他的小说《许三观卖血记》是否还能在中国拍成电影？余华说了一段话，颇有意思：他说，当年我很佩服张艺谋，他总是说，如果你小说中的这个细节拍出来的话，电影局的审查一定通不过，如果那个细节拍出来的话，也一定通不过……我就想，他怎么对电影局的想法那么熟悉。后来，按照张艺谋说的，电影拍出来了，还是被电影局禁了。所以，我现在不佩服张艺谋了。至于《许三观卖血记》，很多电影商来购买版权，我都没卖，因为我不想听到类似当年张艺谋的那种话了。<br />
<br />
 <br />
<br />
我的理解是，《许三观卖血记》怎么拍，也不会被允许公映，索性不拍算了。<br />
<br />
 <br />
<br />
张艺谋正是在拍余华《活着》未获得公演之后，才有了巨大转变的，从那之后，他的所有作品都变成了对中共意识形态的阐释，比如《一个都不能少》、《英雄》等等 ――我甚至懒得指责张艺谋了，好歹一个做过真正艺术家的人，不得不屈从于世俗势力，也有可能是无可奈何的。但他最后成了中共御用导演，完全按照既定话语形态，无条件合作，又让人很难同情了。<br />
<br />
 <br />
<br />
前面说过，余华曾经做过牙医，按他自己的说法，为了获得文化馆清闲的工作而去写小说，最初有功利目的，这不难理解，谁都想得到一个最清闲而又能保证生活质量的工作。但长时间写作之后，余华发现自己的人生完整了，在现实中得不到的，可以在小说中获得――白日梦是成为作家的基本条件，我理解，这是一种逃避。<br />
<br />
 <br />
<br />
在中国，很多人都在寻找逃避的机会，比如很多人出国，到美国去，到欧洲去，更多无法出国的人，也在创造各种机会，让自己从现实中逃开。写小说当然是很好的办法，仅仅从这点出发，我对余华羡慕之极。我认识的人中，因为这一点而羡慕小说家的，大有人在。<br />
<br />
 <br />
<br />
余华是这样，别人也是这样，他比别人多的，是才华，所以，他成功了――不是说他成功地赚到很多钱，拥有很大的名声，而是说他成功地逃离了现实。</div>

							
							
							
							
