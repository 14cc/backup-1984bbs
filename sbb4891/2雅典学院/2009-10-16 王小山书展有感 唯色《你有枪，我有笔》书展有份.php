诺奖得主――高行健和赫塔穆勒<br />
<br />
（德国《日报》网络版稿件，其他媒体请勿转载――中国大陆媒体请随便转载，不收稿费）<br />
<br />
诺贝尔文学奖获得者中，有两个人拥有用汉语写作的能力，一个是美国人赛珍珠，她于 1938年获奖，一个是法国人高行健，他于2001年获奖。不同的是，赛珍珠一直是美国人，在中国生活过很多年，高行健曾经是中国人，从1988年开始，一直生活在法国。相同的是，他们的获奖都曾经有过巨大的争议，赛珍珠获奖被一些人称为诺贝尔文学奖的耻辱，高行健获奖时，我给我的一位朋友，与高行健合作过的话剧导演打电话道喜，他的第一反映是――啊，他凭什么啊？<br />
<br />
中国很多作家也觉得，高行健表不了汉语写作的最高成就，那年，中国官方操纵的作家协会对高行健的获奖更是不满，称之为“一个笑话”。当然，在我眼里，更大的笑话是，官方不准中国媒体报道高行健获奖的消息――如果你让中国政府不快，即使你获得了诺贝尔奖，中国读者也不会知道，高行健和达赖喇嘛（他曾经获得诺贝尔和平奖）都有过这样的遭遇。<br />
<br />
昨天（2009年10月16日），在法兰克福书展上，至少有两个诺贝尔文学奖获得者举办讲座，一个是今年获奖的德国人赫塔穆勒，一个就是高行健。两个人有一个共同点，就是她们的身份，都曾经是流亡作家――主动或者被动地离开自己的祖国。<br />
<br />
赫塔穆勒在演讲里提到了中国，说，她的作品《呼吸荡漾》中，写了很多关于“劳教” （劳动教养，就是不经过法庭审判而直接将人逮捕并强迫劳动，通常是重体力劳动）的事情，中国作家对此会有相同的经验。赫塔穆勒更为直接的话是：人们对于共产主义暴行所造成的恐惧，依然没有足够的认识。赫塔穆勒的另一个做法可能让人感到有些极端――她拒绝接受来自中国大陆的媒体采访。我从国内媒体一位编辑处得到的消息，已经有人做了赫塔穆勒作品的中文版代理，但很怀疑，她的作品是否真的能在中国大陆出版。<br />
<br />
高行健的讲座和赫塔穆勒有很大不同，他和诗人杨炼一起，讲座的内容是“跨越两种文化的生活与写作”，但两个人更多的是在讲如何用第二语言写作，如何直面自己的内心等等话题――有趣的是，高行健提到了一个词，叫“自律”，这个词的意思是 “自我约束”，也就是在中国写作时，作家要考虑到写到什么程度才能获得发表的权利――讲座结束后，我跟高行健说：高老师，现在在国内，我们表达同样的意思，已经不用“自律”，而叫“自宫”（自我阉割）了，高行健笑了。他1988年离开中国，再也没回去过，而且声称，只要现政权存在一天，他就不会回去。一方面，我欣赏有立场的人，另一方面，去国太久，他对中国大陆的了解已经有限，不知道从“自律”到“自宫”，两个说法内涵相似，但目下中国的紧张程度，以及官方对意识形态领域的控制，比他的80年代要严重得多。<br />
<br />
我理解，高行健和杨炼是在做文学讲座，当然以讨论写作为主，但还是对高行健的一些说法有不同意见，比如他说，没有绝对的自由，而作家追求的是心灵的绝对自由，这是作家的本分。这当然没有问题，他接着说，即使在已经充分民主化的国家，人的自由也会受到种种限制，他举了一个例子，说“政治正确”就让人难以表达很多思想。他说的固然不错，但我觉得，还是混淆了“1984”的危险和“娱乐至死”的危险，对很多国家来说，面临的不过是“娱乐至死”，而中国，依然是“1984”。高行健获得诺贝尔文学奖，代表的是中国异议作家群体，而他却很少用自己获得的巨大声誉，去帮助这个群体里的其他人，而依然（他很勤奋）完全沉浸在自己的文学和绘画里，这多少有些遗憾。――我当然知道，一切都会过去，而文学会永存，而且乐于看到高行健创作出更多更好的作品，但还是觉得，如果他能用自己的能力去影响中国当下的现实，会更好一些。<br />
<br />
仅仅在这一点上，我更喜欢赫塔穆勒。</div>

							
							
							
							
