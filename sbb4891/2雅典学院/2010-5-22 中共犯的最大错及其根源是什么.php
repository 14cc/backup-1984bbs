																											<div id="postmessage_632373" class="t_msgfont">最大错是革命太彻底<br />
根源是对理性太迷信<br />
<br />
革命太彻底是指，不仅革了政权命，还革了社会命，也革了文化命，而且只用了短短二三十年，几千年形成的制度和观念就完全颠覆。当然，共产革命之前还有辛亥革命和五四新文化，尤其后一场运动对中国古老文化的清算已经比较彻底了。<br />
革命太彻底的根源是，把人看得太牛逼，以为依靠既有知识和即时创能，就能在一夜之间改天换地。用哈耶克的话说就是，“对理性的滥用”。毛泽东那句话是最好的证明：“一张白可写最新最美的文字，可画最新最美的图画。”<br />
殊不知，良好的制度都是渐进而来，它主要依靠自然的力量，或淘汰旧制度旧文化中的坏东西，或继承其中有生命力的东西。它也可能走一点弯路，但总体上会继承历史，而不会割断历史，只有保存并借助历史中积淀的力量，并小心谨慎地发挥理性建构的作用，才能使社会越趋完善。否则，连根拔起、全盘否定、彻底砸烂，只能是中共的前30年。<br />
搞清楚这一点，再对照一下，有些人或许能发现自己还在犯同样的错，即使他极端反共。<br />
