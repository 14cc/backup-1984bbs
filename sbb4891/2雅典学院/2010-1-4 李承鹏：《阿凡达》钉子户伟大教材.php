													<h2>李承鹏：《阿凡达》钉子户伟大教材</h2>
						
						
																											<div id="postmessage_413777" class="t_msgfont">　　其实看了就知道，《阿凡达》是一部反映钉子户抗暴力拆迁的影片，剧情如下：<br />
<br />
　　第一阶段：2154年，一个叫RDA的土地开发公司，来到一个叫潘多拉的地方搞开发，号召大家只要搬家就可以有诱人的赔偿。可那条街居民大多一根筋，说这里是他们祖祖辈辈居住的地方，再多的钱也不干，再好的地方也不去。开发商就很恼火，这家开发公司其实是有半政府背景的，手下还有一些性格暴烈的打手，就像城管，于是开始打。但居民们拥有一些稀奇古怪的自制武器如弓箭、石头，还有违章的大型宠物如长颈马、迅雷兽，男女老少都很难缠，加之老街地形复杂道路崎岖，手机信号都有死角，所以开发商一时得不了手，还伤了不少人，这个时候开发商就想出派“阿凡达”去当卧底的点子。所谓“阿凡达”计划其实是让人化妆成当地居民刺探他们的底牌，了解地形，分化瓦解，为开发商提供最佳拆迁方案。<br />
<br />
　　第二阶段：可是这个叫“杰克”的阿凡达在卧底时不小心泡上了当地居民首领的女儿，发生真感情，卧底就被反卧底，就反过来动员开发商不要强行拆迁，要体恤民意，他还可以帮忙跟居民沟通做工作。开发商毛了，更毛的是城管头子（那个叫夸奇的退役军人），觉得杰克简直丢了城管的脸，一怒之下就率部前去攻打，有道是“给我三千城管，一夜收复台湾”，他们有巨型推土机，有定向爆破，有武装施工队，一时居民望风披靡，还死了抗暴力拆迁的领导人，也就是那女孩的爸爸。<br />
<br />
　　第三阶段：这时杰克终于明白，根本不需要什么沟通，大家只能以暴制暴，他带领居民们用最土的武器与城管殊死搏斗，自制燃烧瓶、弹弓、大型野生的或家养的宠物、把物件卡在城管的推土机的齿轮中……最后一幕很有喻意，武装到牙齿的城管头子被女孩用最土的弓箭射杀，代表强势政权和极端武力的一方被代表民意和传统一方制服。<br />
<br />
　　看了这部钉子户史诗般的巨片后我有几点感想：<br />
<br />
　　一、正义。各方都觉得自己代表着正义的，比如开发商认为他是拉动了GDP，搞活了经济，为这个落后愚昧的老街带来新气象，只是遭到不明真相群众的抵制；比如城管头子认为叛徒杰克简直不可理喻，作为一名前军人怎可以不听组织上的指挥，那些垃圾生物一排炮打死得了，废什么话；居民们则认为，他们不需要也不接受所谓“美好生活”，对于“美好生活”他们有自己的标准，他们就愿意住在树洞里跟神灵在一起，而不愿去住高级电梯公寓。这个感想是：所有的战争都是因为自以为的正义而起，但真正的正义是――不要轻易改变别人生活的方式。<br />
<br />
　　二、觉悟。影片没有交待是否有漫天要价的情节，而那些居民们住的是黄金口岸，开发商确实也开出了很高的价格，但居民们还是不搬迁，那棵上千米高的大树成为钉子户们伟大的图腾，是一枚参天的钉子，脚下的根蔓则是链接千万年来亲情的基础，他们是为这个活着的。但开发商认为那棵树就是违章建筑，那些居民就是刁民，不醒事体，不支持城市建设，没有牺牲小我成就大我的觉悟。感想是：人类过度发展的结果我们已经看到了，就是玩得自己资源殆尽，就去抢别人的资源。<br />
<br />
　　三、自焚。居民们在高科技的飞行器来到之时还不躲避，还倔强地伫立在那棵大钉子前，其实是在玩自焚。自焚不划算的，根本感动不了开发商，推土机、飞行器里的城管们看着居民烧成碳化物后还哈哈大笑，觉得这些生物真的很二逼。所以以暴制暴的杰克才是对的，他带领大家直扑过去，一顿乱打，城管们才惊慌失措，居民们才换回了家园。<br />
<br />
　　四、叛徒。作为人类的我生平第一次为外星人而背叛了人类，当外星土著与地球城管们搏斗时，我是悄悄站在他们那边的，城管们每回向外星人开枪时就紧张，每死一个外星土著就黯然神伤，那些大鸟每抓下一架飞机时就很想鼓掌，及至躲杀城管头子时，我生怕他复活过来希望那个外星土著美女赶紧补上一箭……估计这些叛徒的念头很多看了影片的人也是有的，因为无论中国人外国人都仇恨强行拆迁的事情（中国人为甚），大家就代入感了，就同仇敌忾了。其实那些外星土著长得并不好看，但是看着看着就觉得她们很妩媚了，因为，所有的生物不是因为符合人类的审美标准而美丽，而是因为心灵的相通而美丽。<br />
<br />
　　五、很多中国电影人都在评论《阿凡达》在电影技术上的领先，也说过这部片子是视觉盛宴但内容一般。我就知道中国电影为什么落后了，土鳖的中国电影人根本不知什么是好作品，他们要么把一部作品的主题搞得神神叨叨，比如《三枪》，要么搞得高层建瓴、文以载道，比如……很多。好的作品就是反映人性的，而不是D性或政府性的，中国电影人也不是不知道这个，他们先是不敢，后便不能了，这方面功能被割掉了，所以中国电影人大部份是太监，中国电影就是太监电影，我们拍点被矫改过的历史片就可以了，幻想片是肯定不行的，不是我们技术不行，而是我们想像力不行，不是想像力不行，而是不敢对未来负责任，只想对有关部门负责就可以了。这时候，电影就不是电影，而是有关部门和有关精神的皮影。<br />
<br />
　　有唐福珍、潘蓉、最牛钉子户，还有河南、四川、上海最强悍城管，还有拆迁办与非常中国的《城市房屋拆迁管理条例》，怀疑卡梅隆是悄悄在中国卧底多年后才想出这个剧本的，只不过把结尾弄得光明一些。总之，《阿凡达》是一部讴歌钉子户抗击暴力拆迁的成功典范，不论战术上还是战略上都值得借鉴。<br />
<br />
　　技术上中国电影落后五十年，人性上中国电影落后五千年。</div>

							
							
							
							
