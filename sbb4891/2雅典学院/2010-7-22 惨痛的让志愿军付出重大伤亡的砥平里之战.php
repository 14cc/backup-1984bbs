　　<br />
　　15日白天砥平里得到空援优先权，前后有131架次，但不是空中力量决定当天结果。<br />
　　<br />
　　主要事件发生在下午约四时三十分，美国老兵回忆，当时第一骑兵师第五团前来解围，把约250名志愿军士兵逼入第23团的火力网，大部消灭，然后突然之间，志愿军部队开始溃退，美第23团第2营营长詹姆斯．爱德华中校称：“这让整个中国部队开始惊慌失措，他们从散兵坑爬起来逃散。我从来没有看过，也从来没有预期会看到这种景象。防御阵线周遭的山丘上都是逃跑的中国士兵！空军猛炸、炮兵猛轰、防空机炮怒吼、所有的迫炮、无后座力炮、以及步兵都在开火射击！目标数量远超过我们武器的数量。真是难以相信的一天！就这么解围了。”（Coleman J. D., Wonju: The Gettysburg of the Korean War, Brassey, Inc: Dulles, Virginia, 2000, page 222.）<br />
