													<h2>共产党有两个洗脑比较彻底的地方：军队和监狱</h2>
						
						
																											<div id="postmessage_796265" class="t_msgfont">中共的政宣工作确实厉害，这一套都是从列宁斯大林那学来的。共产党有两个洗脑比较彻底的地方，一个是军队，一个是监狱。军队呢，不用说，在里边呆几年，出来就是机器人，处事，为人，一切都是机械教条的，思维、言论、价值观都是党化的模式，都是按党的训条修理成的，在军队里，原来就头脑简单的人会变得更简单，执行命令就是天职，下级军官和士兵基本是一群冷血动物。原来头脑精明的人会变得更精明，比如象雷锋这样的，靠投机钻营就能得到升迁，官做的越大，心术越坏。大部分复员转业军人，基本上都是党的忠实奴才走狗。所以说“解放军是革命化的大熔炉”。 <br />
<br />
监狱也是军事化，除了放屁，一切都要报告。与世隔绝，整天灌输的全是党的理念，由不得你思考别的，你所面对的一切根本就没有真实。当然，对江洋大盗和惯犯而言，灌输什么也没用，监狱对他们来说反而是所犯罪大学校。但是共产党会利用刑事犯监督政治犯，毕竟对政权来说，政治犯比刑事犯更危险。所以共产党对政治犯的教育是特别看重的，只要有感化可能，更会动之以情晓之以理，时不时的还搞些小恩小惠，久而久之，本来是反动思想，也转变成了“进步”思想，据说这叫“斯德哥尔摩综合症”。 <br />
<br />
这两个场所，都是培养傻球的地方，原来挺有头脑的一个人，进了这两个地方再出来就是党奴二傻了。这两个地方的洗脑方式类似于北京填鸭或者强奸，填鸭就是定时，定量，把鸭嘴撑开就灌，直接灌进胃里，进肚的东西能不消化吗？强奸，更不用说，受精就得怀胎是吧？哪怕是仇人的后代，也是自己身上的血肉了。这都是强制性的，不接受也得接受，整天看到的，听到的都是当局的宣传，唯一的文化资源是马列主义毛泽东思想，在这种强化教育下，即使原来对马列主义一窍不通嗤之以鼻，那么天天熏，也熏臭了，天天染，也染红了，结果从监狱里出来的政治犯，大部分都变成了又臭又红的马列主义者，头脑僵化，跟不上时代，只知秦汉不知魏晋，因为“洞中方数月，世上已千年”。 <br />
<br />
把党奴二傻再进一步训练，就是特务了，因为经过了强制性的奴化教育马列主义熏染，被训练成特务就很容易了。所以，有人说蹲过共产党监狱的人都是英雄好汉，我并不赞成。从共产党监狱里走出来的，并非都是好汉，在里边经不住折磨虐待而屈服“认罪”的，这是常情常理，可以理解，还属于正常人，出狱后再接着干自己的理想事业，那更是好汉一条。但是被改造成特务的，就不是正常人更不是好汉一条了，这类人，还常常有过“立功”表现，比如出卖他人什么的，OK，只要有了一次“立功”表现，那就等于把自己的灵魂交给了魔鬼，从此就上了中共这条贼船，将永远死心塌地的“戴罪立功”，以报答党的不杀之恩。<br />
