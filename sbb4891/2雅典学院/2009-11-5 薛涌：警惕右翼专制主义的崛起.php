													<h2>薛涌：警惕右翼专制主义的崛起</h2>
						
						
																											<div id="postmessage_314816" class="t_msgfont">最近的中国，有一股右翼专制主义的思潮正在崛起。这种思潮的危险在于，它的倡导者常常是过去的自由派人士，对抱有自由主义价值的人有相当的迷惑性。我们如果对之丧失警惕，使之占领我们的体制，中国现代化的转型就又添一番曲折。<br />
<br />
这种右翼专制主义的思想，在一些主流经济学家的言论中已经体现得非常清楚。比如他们几乎一致排斥老百姓对改革的参与，对网上民意充满了恐惧。从张维迎的“正确的观点不用投票”，到樊纲的“网民不代表民意”，他们想方设法地阻止而不是鼓励老百姓的政治参与，把老百姓的心声妖魔化为“非理性”。《经济观察报》干脆把那些“希望把争论扩大到全社会，进而影响政府决策”的人归之为“反改革势力”，暗示改革只是一小部分人的特权。<br />
<br />
话说得最明白的，当属清华大学教授李稻葵。他公开提出发展中国家民主要缓行，对法治“不能神话”。他不希望老百姓参与关系到自己切身利益的公共讨论，要回到“不争论”的时代，甚至反对公开利益集团的冲突，构想着一个没有任何透明性的社会。他还具体地指责媒体不负责任，搬出所谓的美国模式，认为由几个大财团集中控制的媒体才是负责的，暗示对媒体要加强集中管理。他甚至耸人听闻地声称，现在网上对主流经济学家的一些攻击在美国已经构成了诽谤罪。<br />
<br />
真是如此吗？我在美国生活了十几年，还没有看到网民因攻击公共人物而被判诽谤罪的一个案例。相反，老百姓攻击公共人物受到充分的法律保护。比如2004年总统大选时，一批越战快艇队老兵组织起来攻击克里。他们不仅是网上骂骂，而且拍摄电影，在全国各地的电视上放映。连保守派的评论家也指出他们的片子充满不实之词。克里在大选之中和之后都曾考虑起诉对方，后来还是不了了之。在美国，小人物“诽谤”大人物，在法律上享有非常充分的保护。美国在建国之初，政府一度曾经想管管媒体。第一，那时确实不少人在报纸上匿名造谣诽谤；第二，当时和法国就要发生战争，保持稳定非常重要。于是，亚当斯领导的联邦党人，于1798年在国会通过了“煽动法案”，严格打击对政府官员的攻击和诽谤。然而，这一法案变得如此臭名昭著，促成了亚当斯在1800年大选中的失利，联邦党也很快寿终正寝。甚至亚当斯晚年还极力试图撇清自己和这个法案的关系。不知道在美国读过书的李教授是从哪里搬来的美国规矩？中国如今竟出现写本小说也被判诽谤罪的事情。请问李教授希望把媒体管成什么样子？<br />
<br />
李教授看不上印度的民主，但是印度有些事情却出奇地对他的胃口。比如，他指出《印度快报》非常严肃负责，理由之一就是主编是个经济学博士。另一个理由，是人家监督政府，但“决不会对政府官员进行人身攻击，所以政府官员大都是她个人的好朋友”。这家报纸知道一位国家领导人是同性恋，而且和一个未成年人有某种关系，但在有确凿证据的情况下拒绝报道。<br />
<br />
这就是李教授心中的理想社会：政府官员和媒体人士都成为个人层面的好朋友，政府官员和未成年人发生性关系，媒体帮助掩盖！这样媒体还能监督谁呢？大家还记得克林顿的性丑闻吧。那还不涉及未成年人，但媒体连篇累牍。许多美国的老百姓反对因为这种私生活而弹劾总统，但没有人说这种事情不该报道。耶鲁一位大教授，因为和未成年人发生性关系，不仅被媒体追踪报道，而且现在还在联邦监狱服刑。李教授等于公开提倡媒体要保护罪犯！仿佛一个人只要是个官，犯了罪也属于私生活。<br />
<br />
从主流经济学家的种种言论我们可以看出，他们希望通过加强对老百姓的管制，而不是增加老百姓的权利，来寻求一种经济发展的道路。他们希望建立某种垄断性的权威，来抑制老百姓的政治参与。这是一种典型的右翼专制主义的寡头模式。<br />
<br />
哈耶克认为，从右翼专制向民主过渡，比从左翼专制向民主过渡要容易些。看看战后的世界历史，这话当然有些道理。但是，右翼的专制也是专制。民主才是我们的社会理想。在1980年代，改革刚刚开始，“两个凡是”的左倾势力是改革的主要障碍，所以那时反左是我们首要的历史使命。如今，当年那些原教旨的左派基本上已经退出历史舞台。中国已经经历了深刻的历史转型。在一个新的历史关口，右翼专制主义正在成为对自由的主要威胁。主流经济学家们利用人们对左派的恐惧，口口声声地说要“骗出一个好的制度”。我孤陋寡闻，见过“此地无银三百两”式的“骗”，还没有见过“此地有银三百两”式的“骗”。既然要骗，怎么能先把自己的底牌告诉人家？看来，他们要骗的并不是左派。他们是通过声称骗左派来让老百姓闭嘴，让追求民主的人们放松警惕。这样他们可以在阻力最小的情况下建立右翼专制主义的体制。这才是骗人者藏而不露的底牌。所以，我们这些梦想着一个经济发达、政治民主的中国的人，到了和他们分道扬镳的时候了。<br />
