													<h2>飞虎队：今天的中国人不是昨天的“中国人”</h2>
						
						
																											<div id="postmessage_476477" class="t_msgfont">在中国人中有一种流传得很广泛的说法，认为中国是所谓的四大文明古国中唯一流传下来的文明古国，中华文化是一种具有连续性从未间断的古老文化，换言之，今天的中国人是两千多年前那些古中原人的后代，我们这是一个古老的民族，中国文化是一个古老的文化。 <br />
<br />
&nbsp; &nbsp;我认为这其实是一种很似是而非的说法。不可否认，东方文明或者说是儒教文明，相对于西方的希腊罗马文明，基督教文明，还有中东的伊斯兰文明，埃及文明，印度文明，以及消失了的美洲文明，是相对更具有连续性一些，但是这种连续性也仅仅只是相对意义上，中国人总喜欢用贬低别的文明特别是西方文明的做法来衬托出所谓的“东方文明”的源远流长，古老辉煌，实际上这是一种很可笑的幻想及对历史的错觉。 <br />
<br />
&nbsp; &nbsp;首先，如果从种族上来说，现代中国人跟古代的“中国人”（说中原汉人更恰当一些）之间的传承关系，与现在欧洲人跟古代希腊罗马人，现代埃及人跟古代埃及人，现在的阿拉伯人跟古代中东的波斯人等之间的传承关系并没有什么本质上的不同。我曾经阐述过了，今天的以汉人为主体的大多数中国人与两千年前的中原古汉人并没有什么血缘上的必然联系，甚至与南宋时期的中原“汉人”都没有太大的联系。实际上，历史学家已经证明了：只有现在福建浙江等地的客家人，他们才是纯正的古汉人，他们说的客家话才是正宗的古汉语。而至少现在黄河流域及其以北的所谓“汉人”，只是在两千多年的民族大迁徙和大融合中部分程度汉化了的北方游牧民族后代及胡汉混血后代。南方的所谓“汉人”，大部分也是与百越等南方民族逐渐融合的后代。 <br />
<br />
&nbsp; &nbsp;而我们知道，以大汉族主义为出发点的历史观，实际上是把今天的中国在历史上的角色想当然地代入为古代中原汉人政权，例如，对于东晋南北朝时期，辽金宋时期，我们的历史观都是毫不犹豫地把中原汉人政权作为理所当然的正统来看待，这是一种很荒谬的历史观。 <br />
<br />
&nbsp; &nbsp;特别是在如何看待历史上中原地区各民族之间的战争冲突的时候，这种错误的历史观显得尤为突出，基本上所有的“汉人”及一部分“少数民族”都是想当然地把古代中原汉人政权当作是“自己人的国家”来看待，把其他非汉人政权当作是“入侵者”“敌人的国家”来看待，真的是这样吗？为什么不可以从第三者的角度来观察历史。 <br />
<br />
&nbsp; &nbsp;中国人总是爱指责别人“数典忘祖”，可笑的是，中国人又有谁不是“数典忘祖”的呢？每个人往上数三代，恐怕都不知道自己的曾祖一辈姓甚名谁，从哪里迁来，更不用说曾祖的曾祖了，这也不过才一两百年的时间，搞不清楚自己曾祖辈的中国人，却硬要牵强附会地把一两千年前的某个显赫的人物看作是自己的祖先，这不是很荒谬吗？说不定跟你这些所谓的“祖先”作战的某异民族战士才是你真正的祖先呢。 <br />
<br />
&nbsp; &nbsp;这种做法跟历史上那些入主中原的北方游牧民族统治者都习惯于伪造冒认一个前代的所谓的“华夏始祖”是有异曲同工之妙的，实际上，今天中国“汉人”中很多的大姓人口都是这样的古代北方胡人伪化为“汉裔”的后代。 <br />
<br />
&nbsp; &nbsp;前面所述的那种错误的世界历史观通常认为，除了中国，其他的古代文明古老民族都已经消失了，真的是这样吗？那些曾经在亚非欧各古文明区域生活过的人们，他们是在某一时候都突然死绝了吗？今天在这些古文明地区生活的人们又是突然从哪里冒出来的？ <br />
<br />
&nbsp; &nbsp;其实他们从来没有消失，他们只不过在几千年来的民族大迁徙和大融合中不断地重新组合，由一些旧民族又形成了一些新民族，他们的文明并没有消失，只是在不断吸收，演化。 <br />
<br />
&nbsp; &nbsp;而中国地区相对来说要封闭一些，这种重新融合的频度和深度要小一些，变化缓慢一些，但是这绝不是说古中国地区是一成不变的，是不受任何外来影响的一个独立体系，封闭体系，纯净体系。 <br />
<br />
&nbsp; &nbsp;西方人通常并不认为他们跟所谓的“古代文明”有什么必然的直接的关系，他们通常是从一种第三者的角度来观察远古中古历史，在历史研究中不掺杂太多的感情因素，没有太多的历史包袱。而中国人却偏要牵强附会地把现代的中国看作是几千年前那些古汉人文明一脉相传下来的结果，以此来满足自己那点小小的虚荣感。 <br />
<br />
&nbsp; &nbsp;从文化上来说，所谓的“中华文化”说白了就是“汉文化”，说到“汉文化”，不客气地说，所谓的“汉文化”其实是一种很腐朽的文化，其实质就是以儒教为核心的“伪君子文化”“太监文化”“奴才文化”。 <br />
<br />
&nbsp; &nbsp;太监这种封建产物在东方几千年的封建国家历史上占据了重要的政治位置，从东汉的阉党之乱一直到明代的宦官专权干政，可以说是很深刻地影响了东方封建国家的政治发展和文化发展的，因为作为封建政权最高统治者的皇帝，对他来说，太监是最可以依赖的心腹，他们也往往乐于依赖利用太监治政。而这种太监文化也深深地影响到了封建社会的各个层面，并进一步助长了“奴才文化”的发展。 <br />
<br />
&nbsp; &nbsp;而以儒教为代表的“伪君子文化”，自从孔夫子笔削春秋，为尊者讳开始，就注定了是一种与太监文化同质的阴性文化，在经过历代如程朱理学等的发扬光大，更加加强了东方文化的奴性倾向。 <br />
<br />
&nbsp; &nbsp;实际上，我认为，并没有什么所谓的“东方文明”和“西方文明”的问题，我认为更准确地应该区分为“现代文明”和“中世纪文明”，即使是基督教文明，其在中世纪的那些历史部分也是腐朽的专制的。<br />
<br />
&nbsp; &nbsp;而今天中国人叫嚷着要恢复的“中华文明”其实早就已经僵死了。叫嚷归叫嚷，但是又有谁会真的去“克己复礼”呢？谁又能够说得出来所要“发扬光大”的“中华文化”的具体内容是些什么呢？人们日常生活依赖的完全是现代科技成果，社会政治依赖的是现代法律政治体系。连穿的衣服都是西化了的。一方面享受着现代文明的成果，一方面又言不由衷地吹嘘要复兴什么“中华文化”，不是太虚伪了末？ <br />
<br />
&nbsp; &nbsp;这种所谓的“中华文化”只是中世纪封建文化的一部分，博大倒也勉强算得上是“博大”，精深则根本谈不上，只不过是肤浅内容的简单重复而已。在任何一个方面都可以从现代文明中找到更好的替代。人们日常生活依赖的完全是现代科技成果，社会政治依赖的是现代法律政治体系。所谓的“中华文化”也就只剩下一些古代文学艺术作品可供某些沉迷于古董的迂腐者业余娱乐娱乐而已，其思想内涵也有限得很。 <br />
<br />
&nbsp; &nbsp;综上所述，我认为我们的历史教育应该尽早纠正这种错误的历史观，我们没有必要再沉迷于那些虚幻的历史角色想象中去，沉醉于那些历史垃圾，封建糟粕。我们中国是一个年轻的国家，她形成于二十世纪，是一个崭新的国家，我们完全应该也可以是一个崭新的民族，历史上没有“中国”这么一个国家，只有古汉人古胡人建立的若干东方封建政权。 <br />
<br />
&nbsp; &nbsp;我建议我们的历史书在提到古代历史时，不要再用“中国古时候”“古代中国”这一类似是而非的荒谬提法了，不如客观地表述为“某某世纪由某某民族（例如古汉人，契丹人，女真人等等）建立的封建国家政权”，如由古汉人建立的汉帝国，宋帝国，由古代汉化的鲜卑人建立的唐帝国，由古代契丹人建立的辽帝国，由古代女真人建立的金帝国，清帝国，由古代蒙古人建立的元帝国，等等。这样的表述要科学合理的多。这样也有利于实现民族团结。 <br />
<br />
&nbsp; &nbsp;而今天中国人跟古代中原人的唯一一点联系就是还在使用汉字这种方块象形文字，但是都已经面目全非了（我们用的是简化字），并且这种联系并不比现代其他国家民族与他们古代的发源民族之间的联系更紧密多少。虽然说现代的中国人还能够看得懂部分古代的汉字（秦时的篆书甚至还看不懂），但是语法发音等已经有很大不同了，古代的典籍一般人根本看不懂。汉语的语法已经完全西化了。 <br />
<br />
&nbsp; &nbsp;关于汉字与拼音文字的优劣，我不想多说了。这个问题不是业余的语言文化爱好者可以评说的。而包括“民族魂”──鲁迅，赵元任在内的等等文学大师语言学大师都是主张彻底废除汉字，主张拼音化的，我也赞同他们的主张，我认为汉字这种象形文字极大地刺激中国人的感性思维，却压抑理性思维，使得中国人往往都容易激动，偏执，是中国科学文化发展的大障碍，而且也不利于中国与世界进行交流，害得我们的年轻一代要花费青年时的宝贵学习时间的一大半去学习外语，浪费了大量时间，我看这种方块字除了具有有限的一点艺术观赏价值就很少有什么别的优点了。 <br />
<br />
&nbsp; &nbsp;我认为将来中国最好是逐步实行中英文双语并用，在日常生活用品，公共场所，政府公文，等等方面，像很多国家那样实行双语并用，在基础教育中从一开始（幼儿园）就实行双语教学，经过优胜劣汰，然后逐渐废除汉字的使用，最后实现语言上的与国际全面接轨。 <br />
<br />
&nbsp; &nbsp;而我看到很多海外华人，他们好不容易脱离了中国这种腐朽的政治文化环境，到了西方自由世界，却又开始怀念起旧日的老古董起来了，大概是有一点人的怀旧心理在作怪，但是根本原因我看还是这种错误地把今天的中国当作是历史上中原古汉人封建国家政权的自然延续的历史文化传承观在起作用吧？ <br />
<br />
&nbsp; &nbsp;我想这可能也是中国人那种根深蒂固的大一统观念及封建专制意识的顽固的思想根源所在。 <br />
<br />
&nbsp; &nbsp;而我想说的是，我们今天的中国人，不是昨天的“中国人”，我们跟历史上在这块土地上那些已经融合消亡了的古民族（特别是古汉人）没有什么血缘上文化上的必然联系，我们完全应该是而且可以是一个崭新的民族，一个崭新的国家，我们中国是一个比什么美国，英国等等都要年轻得多的国家，我们要扔掉那些历史的垃圾，不要再受这些封建糟粕的毒害，焕发出青春的活力。 <br />
<br />
&nbsp; &nbsp;我认为鲁迅和毛泽东是对中国了解得最深刻的两个人，鲁迅看到中国人思想中那种深深的劣根性及奴性，看到了东方封建文化及历史是吃人的历史，吃人的文化，因此主张全盘西化，抛弃所谓的“中华文化”历史垃圾，废除汉字，号召青年人不要看中国书（指古书），无疑是非常正确明智的。 <br />
<br />
&nbsp; &nbsp;而毛泽东也是一个深刻了解中国人劣根性的人，他也曾经试图从思想上改变中国人，不过他却最后走到了利用这种劣根性来为自己进行争权斗争的相反路子上去了，或许是他自己的劣根性也太深了的缘故吧。他发动的文化大革命，给中国带来了大灾难，其动机也是各说纷纭，但是文化大革命也带来一些正面的作用，就是对所谓的“中华文化”历史垃圾的全面清扫和破坏，不然我们今天的现代化之路不会进行得这么顺利。 <br />
<br />
&nbsp; &nbsp;而很多痛恨毛共专制的右派人士却对这一点痛心不已，特别是一些海外人士，或许还是上面所提到的那个历史观的原因吧。他们一方面追求向往民主自由，一方面思想中又扔不掉这些儒家文化的历史垃圾。 <br />
我特别不能理解的是，这些海外华人，他们自己不能够摆脱这种历史包袱也就算了，他们还要强迫自己的下一代去学汉语，去学习所谓的“中华文化”，这不是毒害下一代吗？我认为，如果真心为了我们的下一代的幸福着想，真心想要根除历史遗留下来的封建专制思想，就应该让下一代彻底地抛弃汉语，从一开始就让他们完全的使用英语等拚音文字，尽量地做到不用汉语，不看中国书（指古书，这是鲁迅先生的主张），不要沉迷于封建古董历史垃圾里面。让他们成为“新的中国人”。 <br />
<br />
&nbsp; &nbsp;我想中国要实现现代化，就要从思想上先实现现代化，就是要彻底地纠正这种错误的历史观，彻底地抛弃汉字等，使用拼音化语言。彻底地抛弃一切历史垃圾，封建糟粕。<br />
<br />
&nbsp; &nbsp;而现在即便是二三十岁的中国人，要彻底纠正这种思想观念恐怕也是很难了，希望在下一代身上，但是我们仍然可以做到尽量地不使用汉字，多使用拼音语言，不看中国书（指古书，这是鲁迅先生的主张），不要沉迷于封建古董历史垃圾里面。我们要扔掉那些历史的垃圾，不要再受这些封建糟粕的毒害，焕发出青春的活力。去创造新的文化，新的民族。</div>

							
							
							
							
