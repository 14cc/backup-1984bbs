													<h2>艾晓明：作品被禁 人身安全</h2>
						
						
																											<div id="postmessage_287894" class="t_msgfont">感谢各位朋友的关注。我于国庆敏感期之后到北京，原本是参与香港采风公司举办的两岸三地纪录片巡展活动，因为我的作品《开往家乡的列车》，入选今年的2009年香港华语纪录片节，而两届华语纪录片节都是该公司举办的。在首都师范大学中文系研究生的课堂上，其实人很少，不到二十人，放映《列车》不到5分钟，就被校方课堂上赶出来。在中国青年政治学院，由于老师的坚持，放完了《我们的娃娃》；但取消了与学生讨论的环节。原安排去北大，结果连北大的门也没进就取消了讲座。之前在9月底，机票已订，复旦大学方面指令我不得到场，令邀请老师不得不赔付我的退票费。在毛泽东思想指导下、以阶级斗争为纲的这场大戏中，我被派定了老鼠的角色，各路白猫黑猫杂色猫竞相上场追捕老鼠，给学生上了一堂生动的政治教育课，达到了纪录片无法达到的效果。<br />
&nbsp; &nbsp; 我无法到达放映现场和香港的观众交流，我写了下面这几句话――<br />
&nbsp; &nbsp; 我带着这个作品，仿佛带着这些夭折的孩子们的亡灵；我希望，在这个放映的73分钟里，他们的灵魂，能够得到在座各位观众的接纳和珍爱。在这一刻，这些小天使的羽翼，飞翔在我们头顶。<br />
&nbsp; &nbsp; 遗憾的是，在我经过的内地一些大学，包括居于中国大学排名之首的北京大学，一些人动用了所有的权力，把这些孩子们的影像关闭在外，仿佛要令他们死无葬身之地。我要问的是，仅仅是在几个月前，国家主席还在汶川倒塌的中学校舍前，向九万多遇难者默哀，其中，难道不包括这五千多个尚未公布名字的孩子？<br />
&nbsp; &nbsp; 究竟是什么原因，你们要如此抹杀对孩子们的记忆？<br />
&nbsp; &nbsp; 在我的影片里，依然可以听见：一个女孩的歌声、一些幼儿园小朋友在迎奥运宝宝运动会上的欢闹；还有，打工的妈妈读着孩子在地震前一天的母亲节发来问候的短信。但是，在现实中，孩子们永远地寂静下来了，千呼万唤也换不回他们的生命。一部纪录片，不过是投射校舍大灾难的几许光影而已，全无任何实质；谁能告诉我，可怕的到底是什么？<br />
&nbsp; &nbsp; 香港放映的主持人要我对不能谋面的观众说几句话，我只能写出这样几句：我希望，人们用这样的方式――观看和讨论――哀悼并告慰孩子们的亡灵，他们是四川人的娃娃，也是我们在座所有人的孩子。<br />
