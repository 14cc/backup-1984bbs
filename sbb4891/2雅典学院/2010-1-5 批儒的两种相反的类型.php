													<h2>批儒的两种相反的类型</h2>
						
						
																											<div id="postmessage_414960" class="t_msgfont">批儒存在两种截然不同的类型，一种是文明类型，只在理论上采取理性的方法加以论证性的批评，手段是文明的，目的也是文明的；还有一种是流氓无赖似的野蛮类型，不是使用行为暴力，就是使用语言暴力，比如秦始皇、张献忠、红卫兵和毛泽东即为后者。<br />
前者批儒是坚持以文明的方式，站在普世价值的立场上，是为了促进文明；后者是以野蛮方式，为了促进野蛮和强权，即使胜利了也是流氓和强权的胜利。我希望大家严格区分这两种批儒类别。<br />
<br />
虽然自由民主“文明类型“和流氓无赖“野蛮类型“都可能会反儒批儒，但是两者的出发点和手段方式却截然相反！<br />
所以，不能因为为了反儒而采取后者的方式，否则其结果也只能是“促进流氓无赖的野蛮反文明“获胜罢了，犹如文化大革命，那样“反“还不如“不反“！<br />
