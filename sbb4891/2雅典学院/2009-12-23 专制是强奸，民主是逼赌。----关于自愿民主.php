																											<div id="postmessage_390415" class="t_msgfont">专制是一种强制，民主也是一种强制。<br />
<br />
民主是你必须参与赌博，并接受赌博结果。<br />
<br />
铅笔社邓新华等人宣扬的“自愿民主”不是民主，是自由。民主和自由是不同的东西。民主和专制都是自由的敌人。<br />
<br />
自由是一种价值，民主是一种手段。有纷争才需要民主来解决，没有纷争的时候逼别人参与民主，就是逼赌。<br />
<br />
公共领域的问题用民主来解决，是因为公共资源必须共享。<br />
<br />
争取自由，就是守住群己权界，压缩公共领域，尽量避免被强奸或逼赌。<br />
<br />
自愿民主，就是拒绝逼赌。<br />
<br />
在公共领域的自愿民主，就是国家分裂，小国寡民，自愿独立，地方自治，迁徙自由，换国籍比换裤衩还容易。如果按照瑞士的玩法，中国需要分裂为几千个个不同的自治区。<br />
<br />
同一个国家，还得有不同的法律。比如说，马来西亚的法律是双轨的，穆斯林有穆斯林的法律，非穆斯林有世俗的法律。自愿民主就是一个国家可以根据国家宪法制定一系列不同的但是勉强还算得上公平的法律，让你根据自己的信仰选一个你喜欢的。<br />
<br />
自愿民主的结果是：中国分裂成8964个自治州，每个州各有一套自治州宪法和64套适合不同信仰的法律。<br />
<br />
这样也挺好。虽然会穷一点，落后一点，麻烦一点，生活不方便一点，但是自由多。<br />
<br />
海盗和土著社会里，自愿民主会流行。</div>

							
							
							
							
