中国必须坚持当前的制度，西方民主是取乱之道！ <br />
<br />
中国的民主不等同于西方的民主。中国之民主尚不完善，但等它发展到一定阶段后，它将是伟大的制度；西方的民主相对于封建主义是巨大的历史进步，也基本能适应目前已完成工业化的正蓬勃发展信息化的成熟国家，但是它本质虚伪、隐患重重，并且它不太适应未完成工业化的发展中国家。<br />
<br />
1.说西方民主不太适应为完成工业化的发展中国家，这是有太多例子可以佐证。远的不说，就这几天，非洲岛国马达加斯加出现的动乱就是明证！马国不是民主国家、自由社会么？那么在美、英比较稳定成熟的“议会、大选”机制，怎么到了马国就根本不稳定了呢？怎么出现了强行夺权、社会对立、流血伤亡的事件呢？还有无数例子：巴基斯坦、智利、乌干达、乌克兰、波黑、利比亚、刚果、黎巴嫩、泰国、菲律宾、印尼、墨西哥。。。。不胜枚举的国家，几十年来发生了多少次诸如政变、暴乱、骚乱、社会对立、极度贫富悬殊等等的非“民主”事件？？？！！！这些国家的人民，很多时候连基本的生存和健康权利都得不到保障，谈什么更高层次民主？？？！！究其原因，症结就在一点：它们奉行的西方民主制度，在自身工业化未完成、条件不成熟的情况下，不但没收获成熟工业化国家所获得的民主成果，反而导致了“泛自由化”的恶果！这就是乱源！！！！<br />
<br />
2.说西方民主“基本能适应目前已完成工业化的正蓬勃发展信息化的成熟国家，但是它本质虚伪、隐患重重”，同样是事实证明的。<br />
首先，西方发达国家和其他一些基本完成工业化的国家例如瑞典、冰岛、韩国等，他们的西方式民主已经比较成熟，能够稳定执行“议会”和“大选”路线，所以说西方民主“基本能适应目前已完成工业化的正蓬勃发展信息化的成熟国家”。<br />
其次，说它“本质虚伪”，这是一点也不夸张的。社科院的房宁研究员最近发表在《人民日报》上的文章把这一点论述得非常精辟，这里我引用原文：“掌握巨大财富与金钱的大资本集团，把现代西方国家多谠竞争与选举演化为一场按商业规则运作的政治推销活动，通过对竞选规则的控制，通过媒体的运作、炒作，控制公众的信息接受，广泛而深刻地影响社会舆论与公众认知，进而影响选明的选择，最终取得竞选的胜利。据美国联邦选举委员会2004年12月14日公布的报告显示，2004年竞选一名参议院议员的平均费用约为251万美元，最高达3148万美元；竞选一名众议院议员的平均费用约为51万美元，最高达904万美元。试想，没有大资本集团的资金支持，怎能进行权力的角逐？不依靠金钱的力量，又怎能在角逐中取胜？在西方资本主义制度下，无论哪个政谠上台执政，实际上都是代表资产阶级和大资本集团的政治力量控制国家政权。”<br />
第三，说它“隐患重重”，核心就在于西方式民主必然导致社会利益及群里的分化，造成社会对立，严重的就发展到“泛自由化”，前面已经说过，这导致了大量不发达国家的社会动荡。即使在发达国家这样的工业化已完成、“议会”和“大选”路线稳定的社会，虽然并未过度“泛自由化”导致动乱，但尖锐的社会冒盾所造成的社会消耗同样是不容忽视的。法国是典型例子，该国动辄就是几十万、上百万人的大罢工，巴黎年复一年的大规模骚乱等等，造成了多少经济损失和人身伤亡？？！其他例子也很多：中国台湾省，从立法机构武斗到在野党发起诉求空虚的“呛声”抗议示威，再到个别行业罢工等等，不一而足；还有印度，其国内的民族和宗教矛盾根源在哪里？为什么前阵子出现孟买恐怖袭击事件？根本就在于它的民主并未普及并同等于各民族群体和宗教派别，事实上的不平等，是其乱源，而印度不是所谓民主国家么？难道“民主=不平等”？<br />
<br />
现在回过头来，说说为什么“中国的民主不等同于西方的民主。中国之民主尚不完善，但等它发展到一定阶段后，它将是伟大的制度”。<br />
1.中国的民主当然不同于西方式民主。说到这里，有些人可能要乱反驳说中国根本没有民主。你如果这么想和这么说就大错特错了！民主的定义，从来就不应以西方民主为标准的。前面已经阐述了西方民主的缺陷，因此它当然不能成为标准！<br />
实际上，民主的定义不应当有一个固定不变的标准，因为民主制度对于一个社会的适应性，必然要随着这个社会的进步发展而不断调整的。社会在经济科技文化上越进步发展，民主制度就要越开放，但也越收敛。<br />
为什么“既越开放，又越收敛”呢？“开放”是指民主程度的提高，例如从封建制的专制到共和制的普选；“收敛”是指名主制度在道德层面上的、范围的紧缩。举例来说，西方经常发生罢工，在法律和民主角度来说，它是合理的；但是从道德角度来说，你罢工影响了交通，造成了行业瘫痪，甚至引发骚乱造成直接经济损失等等，从道德角度讲就是不合理的。当一个社会发展到很高阶段后，注意，我说的很高阶段，彼时人们的觉悟十分的高了，物质水准也十分高了，那么人们就理所当然从心里上认为罢工影响社会运作是不合理的了，因此民主概念就会修改了――人人都觉的罢工不合理了，那么“人人”的民主制度，当然应该要禁止罢工了，这就是“名主收缩”的过程。<br />
中国怎么没有民主呢？中国当然有民主！中国的执政党是代表全体人民而不是代表某些利益集团的，这就是最大的民主保证！中国的一党专政，保障了社会稳定，凝聚了社会力量，使人民享有有保障的生存、健康和发展权，这是最基本的人权和民主！<br />
中国的执政党虽然是一党专政，但从获取公共权力的形式上讲，中国并不比西方更不自由。可以这样比喻：中国共产党是一个巨大通道通往公共权力，而美国则是两条通道，一条叫名主党，一条叫共和党。但绝对不同是：中国的这一个巨大通道，代表着所有人民；而美国的两条通道都只是它们各自利益集团的代表，他们两者加起来，也只不过是美国全体人民的少部分。<br />
中国当前的民主，当然很不完善，其“收敛”的内容过多和超前，而“开放”内容过少和滞后。这就是监督机制缺失的根源，进而是腐败产生的根源。因此，我们必须改革，不断的改革，但是，必须坚持循序渐进的原则！这是稳定的关键！而稳定是发展进步的保证！<br />
我们当前的、正不断循序渐进改革进步的制度是适合我们的，任何其他道路，包括典型的西方民主对我们而言都是行不通的，都是取乱之道！<br />
<br />
<br />
两极分化？<br />
中国目前的确有贫富差距拉大的现象。但是，贫富差距拉大甚至悬殊不是社会主义的专利，你看看阿根廷、墨西哥、印度。。。。甚至包括美国，它们的贫富悬殊大，还是我们大？（请你去查权威数据，不要凭自己的观感就急切下论断）<br />
同时，贫富差距的造成，是市场化深入发展的结果，是改革开放之初“让一部分人先富起来”政策导致的必然结果。<br />
<br />
解决贫富差距，表面看来是一个技术性问题，似乎就是国家调控而已。但是，本质在于，资本主义社会的国家调控，必定是表面化的、不触动大利益集团根本核心利益的，它当然能一定程度上缓解社会矛盾，典型的如北欧国家瑞典这种高福利国家。但是你要看到，这种高福利政策的弊端相对于它的作用，是大得多的，它使社会丧失创造力和能动性。其他的国家，诸如西方民主国家的典范美国，它那样悬殊的贫富差距，也是通过社会保障体系来缓解矛盾的。但是，这种缓解=真正公平吗？<br />
实质上，多数的财富，不还是掌握在大利益集团手中吗？<br />
<br />
你或许要说，中国不也在发展社会保障体系，以缓解贫富差距导致的社会矛盾吗？<br />
是的，没有错。但是你必须看清楚一点：中国的确有很多个人或私有集团相对普通大众来说他们富有得多，但是，中国的财富的大部分，不是掌握在他们手里的；中国的国有资产不但在“质”方面是控制国家经济命脉的，而且在“量”上是占据多数优势的。这就是为什么中国仍然是“社会主义国家”，而不是某些人所谓的“穿着社会主义外衣的实质性资本主义国家”。<br />
<br />
中国的社会主义理想，从来就没有偏离“最终消除不平等、剥削、贫富差距”这个最初的目标方向。只是，对于我们这个仍在工业化发展道路上、并在信息化发展道路上的落后国家，我们的生产力发展逾越不了“市场化”这个必然阶段，而“市场化”必定导致贫富差距。<br />
你不但要看到 “富差距是“市场化”必然的暂时结果、以及“市场化”是我们乃至任何社会的发展所不可逾越的阶段” 这样一个普世现象，你还应当看到：我们的社会总体资本关系到底和西方有着什么样的本质区别！<br />
<br />
贪污腐败进行曲?<br />
首先，“贪污腐败”从来就不是社会主义的专利。这世界上的绝大多数国家都是资本主义，可是你仔细看看，有几个没有“贪污腐败”的发生？只是程度不同而已。<br />
<br />
你或许要说，别人的监督机制好，因此“贪污腐败”是可控的，而中国由于“专制”，必定缺失监督机制。<br />
你这么说，就是把“贪污腐败”看做是社会主义和资本主义的本质属性的区别之一了。<br />
这种认识是相当的错误。事实上，“贪污腐败”这个问题，本来就是一个纯技术角度的问题，那就是―――私欲是各国人民共有的，它是腐败产生的内因；而遏制腐败的关键，就是监督机制。<br />
资本主义的监督机制，在西方各国的发展程度是不一致的。你看到美国、新加坡、芬兰等国家，它们的监督机制的确是比较完善的，因此他们的腐败问题不太严重。但是你看看俄罗斯、印度、墨西哥、巴西、马来西亚等国家尤其是印度尼西亚，个个都是臭名昭著的腐败横行的国家。最近西方某个权威机构――具体我记不清了，你可以去查――发表了全球各国腐败程度排行榜，十分讽刺的是，这个排行榜的前十位都是资本主义国家，例如臭名昭著的印度尼西亚，而一个社会主义国家都没上前十。如果现在有人说：古巴是个腐败严重的国家，那他一定会被有识之士笑死。<br />
<br />
监督机制的发展程度，不但在各国之间发展程度不一样，在每一个国家的历史上也不是一成不变的。看看美国吧，看看它100年前“西部拓荒”时代，你翻翻历史资料就知道，它那时候的腐败是多么流行！<br />
<br />
所以，说了这么多无非是要说明一点：“贪污腐败”不是社会主义的专利，不是“社”与“资”的本质区别，它纯粹是技术层面上的问题，亦即：“贪污腐败”是市场化的必然副产品之一，而监督机制是否健全是解决其的关键。<br />
你敢说在毛泽东时代，中国是个贪污腐败横行的社会么？国际上众多具有影响的人士（例如英国前首相希思），在毛去世的时候对他的评价中，普遍提到一点：毛在中国建立了一个全世界最清廉的政府。而毛时代，不是社会主义么？<br />
<br />
当然，说这些只是为了阐述“贪污腐败”问题的本质，并非是要为中国当下严重的“贪污腐败”问题开脱。<br />
对于这个问题，中国共产党并不是视而不见的，并不比你我这些普通百姓更看不到其严重性。翻翻历史，你会了解到，建国前夕的中共七大，毛就提出了“谨防资本主义糖衣炮弹的袭击、防止腐化堕落”，建国后的“刘青山、张子善”事件等等。<br />
改革开放后的一段时期，中国共产党的确在这一问题的认识上有所松懈了，这最终导致了89年**的爆发――这次事件，人民反对的是贪腐，不是社会主义！只不过在西方势力的刻意误导下，使得人们以为当时是要推翻社会主义制度。经过这次事件，中国共产党有了深刻的教训，把“贪污腐败”问题摆到了“事关党的执政地位的合法性”的高度位置来看待（这是共产党的各种文件上、包括党大会报告上的明确阐述），这些年来的作为大家不是没看到――从“陈希同”到“陈良宇”，各时期各地各大大小小贪官污吏纷纷落马――这不是在只讲成绩不看问题，的确，问题依然相当相当的严峻，我本人不是什么官方的枪手，只是个普通百姓，我没必要可以遮掩这个问题。我只是从一个旁观者的角度来看：这个执政的党和政府，只要它一直认识到自身的这个严重问题并在不断改进，并且它仍走在领导全国人民发展富强的正确道路上，那么它就是有希望的，值得我们继续支持的！！古话说的好：水能覆舟，亦能载舟。如果真有那么一天，这个党和政府不再是人民利益的代表了，人民自然会做出自己的选择！！<br />
看待问题，请用发展的眼光看，请用全面、大局观的角度看！！！！！<br />
<br />
“楼主要保护的是什么?”你问的好，我也正想对这个问题进一步阐述。<br />
我，一个普通的老百姓，一个23岁的刚刚大学毕业没多久的80后年轻人，<br />
要继续坚决维护的就是：中国当前的特设社会主义道路，中国当前的共产党领导的一党专政！<br />
要继续坚决反对的就是：“西方民主示范”和“自由价值观输出”的虚伪欺骗！<br />
在我的原帖中，我已经深刻的阐明了：为什么西方民主是虚伪的，为什么我们要坚持当前的道路和体制。你如果愿意，请再回头读一读，带着客观审慎的眼光去读，不要武断地认为，那不过是国家宣传的官样文章而已。<br />
我们可以通俗的这样比喻：买东西还要货比三家呢，我们选择道路和制度，当然要比较历史上和现在的所有可能选择。<br />
事实已经无情的向我们展现了：工业化未完成的广大发展中国家们，奉行西方式民主的，例如阿根廷、墨西哥、印度、印尼、菲律宾、马达加斯加、黎巴嫩。。。。。。。等等等等，它们发展成什么样了？阿根廷等国发展资本主义的时间不比美国少多少，可它的经济规模、人民生活水平怎么样？？<br />
奉行西方那一套并所谓“成功”的，不就仅仅是那些发达国家和少数次发达国家和地区吗？<br />
可你必须了解的是，他们的所谓“成功”，是有着多少历史时代背景促成的。英、法是典型的通过殖民时期在全世界掠夺资源、土地而发家的；美、德等国是赶上了第二次工业化浪潮，并在其中扮演了先进者的角色，因此发达起来的；日本的明治维新，使得它得以跟上欧美的工业化浪潮，为它的发展奠定基础。其他诸如意大利、加拿大等等，也是伴随着第二次工业化浪潮发展起来的。而广大的其他资本主义阵营的“较成功”国家和地区们，如“四小龙”、澳、新，包括战后的日、德等，他们今天的成功离不开那时候的大背景。它们隶属于美国同盟阵营，没有封锁，赶上了战后“黄金二十年”，在稳定的条件下发展不起来才叫他们自己的罪过！<br />
你再反观那些处于不稳定条件下的，例如中东的各“火药桶”国家，例如各个冷战时期被民主阵营漠视的、不具备各项优势条件的拉美、南非、亚洲的所谓“民主”国家，几个发展上去了？<br />
当然，能不能发展上去，不光是大环境、大背景的因素，也同样取决于各自的内因。那些长期稳定、却并没发展上去的国家，例如墨西哥、智利、土耳其等等，它们彼时没有明晰的发展战略、没有凝聚的国民决心，也理所当然上不去。<br />
彼时的社会主义阵营的那些东欧国家们，例如波兰、捷克等等，身处美苏争霸漩涡当中，并且靠山苏联自己也没能全面发展好，它们的没落是必然的。<br />
反过头来看，中国即使在改革开放以前，由于坚持自力更生，由于高度的凝聚力，由于在许多方面的正确战略，同样取得了举世瞩目的经济成就！----你不必急切的武断反对，你看看建国前和1976年两个时期中国经济数据的比较，再看看几十年来来自西方政界、学界、经济界对这方面的客观评价，你会发现：我们即使在那样一个外部封锁、内部动荡的年代里，由于自力更生、坚持工业化和科学发展等等一系列正确的战略，我们依然取得了举世瞩目的成就！用西方人的评价就是：中国毛时代完成的工业化是个奇迹，因为西方普遍花了上百年才完成的。――――当然，这个说法我们要客观看待，不能一味抬高自己，毕竟，所处的环境不同，他们花上百年，那是因为他们是探索者，而我们发展的时候，是一个追随者的角色，前人为我们提供了经验。但是只要你横向比较，比比那些同时期发展却不成功的大量所谓“民主”国家们，你就会心里有数。<br />
<br />
回顾了历史，我们再展望现今和未来。<br />
改革开放三十年，发展了，进步了，富强了，还是落后了，倒退了，更贫穷了？<br />
前面已经说过：买东西都要货比三家。我们拿我们的成绩和问题，比比广大所谓“民主”国家们的成绩和问题，到底谁好谁坏心里还没个数？<br />
“实践是检验真理的唯一标准”这真是一句至理名言！实践告诉我们，我们所走的道路是正确的―――这绝不仅仅是经济发展战略的正确而无关道路体制的正确，事实上，没有我们当前的道路和大的体制（注意我说的“大”，因为这个大的体制是正确的，但是它仍然需要不断改进，循序渐进的改进），我们就不会有稳定的环境，就不会有高度的思想统一，高度的民族凝聚力，谈何高速发展？？？<br />
<br />
你问我“保护什么”，我想，其实你肯定在心里还问道：你到底为什么要“保护”你所保护的东西。<br />
再次在这里明确的告诉你为什么：我之所以“保护”它、“维护”它，原因正是我上面所阐述的！<br />
<br />
我发自良心的表明：我只不过是重庆一个刚刚大学毕业没多久的23年轻人，我与国家的宣传机器毫无任何关系。我所说的这一切，只不过是在自己阅读了很多、看过了很多、思考了很多之后，作为一个热爱祖国、关心民族命运的赤子，发自肺腑的铿锵心声！我坚信，我的认识是正确的，绝不是某些毫无见地的人自以为是地评价的所谓“宣传机器的又一个牺牲品”。<br />
<br />
我坚信一点：客观、全面的态度，发展性和全局观的角度，才能正确认识一个事物！<br />
<br />
<br />
&quot;中国应该走自己的道路。民主是要的，但关键看要什么民主。西方式的民主已经在俄罗斯、印度及众多东南亚和其他国家身上试验证明是失败的。<br />
<br />
中国的制度当然要改，但这是一个长久的过程，不是一朝一夕可以完成的，要循序渐进，摸索着前进。那些期盼中国会翻天覆地、大乱一场而变化的人，不仅是理想化的，说难听点，是想误国误民，虽然其中一些人的出发点是好的。&quot;-----------------------真理!!!<br />
<br />
<br />
什么样的国情就有什么样的民主,中国要走适合中国国情的民主道路.要是套用&quot;西方民主&quot;的话,那将是人民的灾难.<br />
<br />
<br />
腐败不是共产党的专利，数数全世界200多个资本主义国家和地区，从美国到毛里求斯，哪个敢说没有过腐败？台湾有陈水扁，菲律宾有马科斯，韩国有卢泰愚、全斗焕，泰国有他信，俄罗斯、英国、美国、法国、德国，你们自己到网络上去搜索搜索，看看有没有！！！！！！<br />
腐败不是“根本制度”的必然属性，只是受“事务性制度”的影响。说白了，监督机制缺失与否，是遏制腐败的关键。中国是社会主义国家，并不代表中国没有监督机制。只是客观地说，中国目前的监督机制尚不完善。<br />
民主的价值是普世的，但是，民主的价值不是唯西方为标准的！<br />
哼，很多人以为，所谓民主，就是西方的大选、游行示威、自由持有枪械。。。。<br />
我国政府说，人民的生存权、发展权，才是最大的民主，你们以为这不过是一句宣传口号吗？<br />
&quot;反华势力亡我之心不死&quot;―――善良的中国人民，别当这是一句杞人忧天的空话！<br />
<br />
<br />
亲爱的人们，如果你关心时政，关心国家发展，关心民族的未来，那么，请你多看看国家的战略政策文件。<br />
