看过了，思考了，再提出你的意见，你的主张，你的反对，你的诉求。<br />
我，一个23岁的年轻人，一个“社会主义制度和共产党专政的卫道士”，在回首了历史、关注了现实、展望了世界之后，依然信仰不改！</div>

							
							
							
							
