																											<div id="postmessage_235163" class="t_msgfont">　　上次在书店，看到《我执》放在显要位置大卖，顺手拿起来翻了一下。不知道是否一贯的偏见在其作用，看其文字，就无端觉得一个油头粉面、像个香烟大小的小男人，捏着一根大雪茄，装腔作势忸怩作态地站在你面前。<br />
　　以前罗胖子问我对梁的印象，我迟疑了一下说，不要问我，我极不喜欢这种道貌岸然的新左。我个人总觉得，在西方受教育，变成一个新左原本正常，如果不是在中国语境下，你大可以一笑置之。然而，对一个半个多世纪发生在中国的苦难事实就在近旁的人来说，有时候，某些愚蠢的话就是非常非常之不能让人接受和理解了。<br />
　　在中国语境下，这种睁着眼睛说瞎话的左棍，要么是智力和认识能力上有问题，要么是人品和道德上有问题，我不知道梁文道属于哪一类。<br />
　　我个人也相信，那些西方学院里皓首穷经、立足于西方自由世界，基于对现实社会批判态度的学院左派，如果对东方世界有深切真实的体悟，恐怕会在中国语境下对其原有的观点表达会有所克制。<br />
　　至于梁文道，唯一一次见面，正好坐他旁边，听他讲佛学体验，讲宽恕，说了一个老奶奶对迫害她的人如何平静地宽恕的例子，说实话，他总结和概括完后，我有种反胃的感觉，心想，什么是姿态和矫情，什么是拿他人的苦难做自己姿态的矫情，这个就是了。<br />
　　在我看，当我不能理解鲁迅当年的刻薄和辛辣时，只要看一看梁文道、余秋雨这样的人，我立即就能理解了。 <br />
