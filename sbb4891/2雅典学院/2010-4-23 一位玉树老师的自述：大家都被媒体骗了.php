<br />
14日灾民徒手救人 无食无水没有援助 <br />
<br />
<br />
<br />
&nbsp;&nbsp;<br />
<br />
<br />
<br />
地震发生时，学生住的整个3层楼房一下子全部塌了，学生都埋在下面了。我们自己动手挖，但是根本搬不动那些水泥石头。我们找了救援队，他们到下午才来了个 铲车，可是根本什么都没干，一个活人都没救出来就天黑了。&nbsp; &nbsp;<br />
<br />
<br />
<br />
我们的邻居一户一户全家被埋的很多，可是我们没有时间去管他们。我们都是自己赶快挖自己的人，用手刨，用木头杆子翘挖了2天。新闻上说救援队救了6000 多人，但他们挖出来的人很少，事实上都是我们自己人挖出来的。&nbsp;&nbsp;<br />
<br />
<br />
<br />
我们把人都往赛马场上拉。我从学校的医务室找来药品(後来药都用光了)，我们把病人都安排到一起，有些安排到大货车上，把所有能用到的被子都给他们用。天 黑以后，我们从烂房子里找来木头，点上烤火。<br />
<br />
<br />
<br />
&nbsp; &nbsp;<br />
<br />
<br />
<br />
救援部队大厦作秀 媒体电视公然撒谎 <br />
<br />
<br />
<br />
&nbsp;&nbsp;<br />
<br />
<br />
<br />
半夜两点的时候，我骑上摩托，回去城里看，城里乱起八糟的。路过一个三叉路口，看见有很多人，有救援队在“救人”。但令人气愤的是，就只有几个人在城建大 厦上面转，下面好多的救援队人员在聊天。当地人说：他们从中午就在这个楼上，现在还在这个楼上，这个楼中午是那样，晚上还是那样。他们根本就没有救人，都 是在演戏。&nbsp;&nbsp;<br />
<br />
<br />
<br />
还有，他们的相片是修改过的。有个记者当着我们围观者的面就撒谎，她说现在是半夜两点钟，救援队还在救人。还有电视台的记者说现在都3点了，还在救援。这 些都是放屁！那些叫中国地震救援的人，只有不到10个人在上面找人，可是他们从中午找到现在，很多大石头都没有搬。那个楼下面有人喊救命，可是里面突然喷 的一声，就着火了，随後就没有声音了，而他们连火都没有去救。&nbsp;&nbsp;<br />
<br />
<br />
<br />
<br />
<br />
他们只在这个地方从事所谓的救人，可是别的地方都没有去。我的学生还在倒塌了的楼层下，他们都不去救啊！我问他们，他们说去了去了，但他们根本没去，我们 那里根本就没有什么救援队。<br />
<br />
<br />
<br />
 <br />
