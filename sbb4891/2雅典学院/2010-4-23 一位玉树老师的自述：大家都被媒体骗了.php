 <br />
<br />
<br />
<br />
【人民报消息】好多话我不敢说，可是今天听了网友的建议，我觉得我还是说出来。但是请网友高抬贵手，不要搜我，更不要被有关部门发现我，因为你们保护不了 我。<br />
<br />
<br />
<br />
14日5点多的时候吧，我还在睡觉。床突然晃了晃，晃得很厉害，差点把我从床上晃下去，我吓了一跳？怎么了？不会是地震吧？可是等了等，没反应，也没有再 晃。外面静悄悄的，人啊狗啊什么的都没有叫。我等了等，加上太瞌睡了，又睡着了，睡了一会，心里想想不对劲，我就穿上衣服，先抽了根烟，这个时候突然地震 了，把我直接甩到地上了。我跳起来就开门，拉了两下门没开，我就冲到窗户前，两锤头把窗户打烂了，连滚带爬跑到院子里，刚出来就什么都看不见了，灰尘把什 么都挡住了……&nbsp; &nbsp;玉树地震了，可是事实上不是你们在电视上看到的那个样子。&nbsp; &nbsp;<br />
<br />
<br />
<br />
我说的话都是事实，而且都是我亲眼看到的。陈述的这些事实都是16号早上10点以前发生的。&nbsp; &nbsp;<br />
<br />
<br />
<br />
首先我要说得是，我们这里没有像电视上说的那样，人死的很少；没有像电视上说的那样，他们在挖着救人；没有像电视上说的那样，有吃的有喝的；更没有像电视 上说的那样，他们半夜还在救人。他们在撒谎，整个的救援行动都是在总理来了以后才慢慢开始的。&nbsp;&nbsp;<br />
<br />
<br />
<br />
玉树地震了，很多人被埋了。房子不是像新闻上说的塌了85%，楼房我看见可能塌了1/3，没有那么多。但是所有的土房子几乎都塌了，而我们这里大部分房子 都是土房子。你们看见的电视上的房子都是在玉树县中心的房子。&nbsp; &nbsp;<br />
<br />
<br />
<br />
人死了最少有3000以上，这个是我看见的。很多户都是一家一户的被埋了，没有人去管他们的，也没有人去统计，但是光我估计有3000人以上。 新闻有很多是不全面的，就是说有些没有报道，还有的是假的。 <br />
<br />
