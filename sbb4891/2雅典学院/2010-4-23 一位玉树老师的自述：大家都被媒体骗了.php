救援部队食住无缺 灾民食水帐篷需靠抢夺 <br />
<br />
<br />
<br />
 <br />
<br />
<br />
<br />
15号他们一天都没有救人，我看见他们还在那个楼上站着。别的地方除了私人救人以外，没有看见救援队和其他人。当天早上我骑着摩托转了整个城去找食物、 水、帐篷、被子等，问了很多police，他们都说没有物资可发。我问不是昨天民主路派出所有发吃的吗？police说没有。这个时候快到中午了，车开始 越来越多，都是周围县上的，很多拉的吃的和帐篷什么的，不过都是私人的。&nbsp;&nbsp;<br />
<br />
<br />
<br />
<br />
<br />
救援部队住在赛马场的上面 ，他们自己有吃的和住的。还有什么考察队啊，单位的救援队都住在体育场里，他们都有吃的和住的。我这里要说一下，我们是牧民，每家都有帐篷，可是有些灾民 还是没有帐篷，所以大部分人还是没有住的。中午我回来以后，看见有的人家在搭救灾帐篷，我跑过去问帐篷是哪里来的，他们说是抢的。这时车多的把路都堵死 了，我从来没有见过这么多的车,整个玉树县都是车。之後,我去了那个地方，但是帐篷都被抢光了。我骑着摩托一直往下走，很多车上的物资都被抢光了。以後我 再也没有见过运东西的车，因为路都堵死了。<br />
