													<h2>刘晓波：韩寒评“大师”已经很客气了</h2>
						
						
																											<div id="postmessage_557849" class="t_msgfont">韩寒评“大师”已经很客气了 <br />
<br />
刘晓波&nbsp;&nbsp;<br />
<br />
韩寒和陈丹青的对话，论及中国现代作家，韩寒说：“茅盾、老舍文笔很差”，“冰心的完全没法看。”后来，韩寒承认提老舍是口误，他想说的是陈丹青提到的巴金。<br />
<br />
就这么几句大实话，居然惹怒了众多国人，加上媒体的炒作，变成了“韩寒再次语出惊人，炮轰众多文学大师！”接踵而来的是上纲上线的炮轰韩寒。<br />
<br />
严肃点儿的评论，指责韩寒“侮辱大师，颠覆大师，向大师泼污水。”但这些人却说不出“文笔很差”的评价错在哪儿，也说不出这几位大师的文笔好在哪儿。<br />
<br />
国人爱作诛心之论，自古皆然，当代尤甚。指责韩寒，也必定要用上，无非是“名利”二字。他们说：韩寒一贯标榜“特立独行”，经常“语出惊人”，实际上是自我炒作。他这次“站在文学大师的肩膀上撒泼，当然可以赚足眼球，甚至也完全可以赚来银子”。<br />
<br />
自从网评在中国风靡以来，其醒目的特点之一，便是文革式叫骂的泛滥。这半年来遭到这种叫骂的人，就有达赖喇嘛、CNN、家乐福、王石、莎朗&#8226;斯通、范跑跑等，现在炮轰韩寒，自然也少不了骂街：“泼妇”、“无赖”、“小丑”、“蚍蜉撼大树，可笑不自量力。”<br />
<br />
我不知道骂韩寒的人所据何在，但就我个人读过的中国当代作家而言，韩寒应该是极少数具有独立眼光的青年作家，不但写小说、赛车，还经常参与公共事件的讨论。我喜欢读他的时政短评，诚实地说出常识，智慧地运用文字，直白、朴素、幽默、辛辣，既轻松又过瘾。在他的时政短评中，我找不到故作惊人之语。<br />
<br />
众所周知，年少成名的韩寒，是知名度极高的80后作家，其博客的点击高达一亿五千四百万，中国个人博客点击率排名第二。这么高的知名度，如此多的粉丝，有必要靠“语出惊人”来自我炒作吗？再说了，如果从审美或文学批评的角度看，韩寒对巴、茅、冰的评价，也绝非特立独行的“语出惊人”，持这种看法的批评家，大有人在。<br />
<br />
在八十年代成名的文学研究者和作家之中，能够说巴、茅、冰“文笔优秀”的人，恐怕不多。专攻中国现当代文学史和从事文学批评的学者，更不可能把三位称为“巨匠”。比如，北大中文系教授钱理群先生认为：“巴金的文体不算精美圆熟，也许还不属于那种可供反复推敲咀嚼的创作。”专攻中国现代文学史的李辉先生说：“巴金的好处在于他的激情，然而，这也正是他的大缺点，他的激情是一种缺乏节制的激情，这注定了他不可能成为大作家，也造成了他的作品思想深度的缺位。”九十年代成名的作家和批评家，我也没见过谁给予这三位以很高的审美评价。比如，文化批评家张闳先生和文学评论家葛红兵先生，也认为这三位徒有“文学大师”之名，作家朱文的评价更低：“巴金一钱不值。”比韩寒还韩寒！<br />
<br />
我这代读过中国大学中文系的人，中国现代文学史是必修课，都知道“现代文坛六大家”：鲁（迅）、郭（沫若）、茅（盾）、巴（金）、老（舍）、曹（禺）。他们六人几乎占据了整部现代文学史，但这样的文学成就排序是政治性的，源于中共的钦定史学和统战策略。回归文学本身的审美评价，茅盾的代表作《子夜》是“政治图解”，只不过比“延安作家”图解得稍微高明点；冰心的早期“问题小说”大都是观念先行的产物，她最有名的《寄小读者》乃现代文学中肤浅抒情的代表，读不下去，很正常。三人中被捧得最高的巴金，也仅是一位有影响的作家而非文学巨匠。<br />
<br />
如果放开眼光，把中国现代文学放入整个世界现代文学中比较，中国现代文学史上就根本没有“文学巨匠”。仅就汉语文学圈来说，中国现代作家对汉语文学写作的贡献，巴金远不如鲁迅、沈从文、老舍、曹禺，甚至不如张爱玲和萧红。正如韩寒“文笔很差”的评价，巴金的最大缺陷是文学语言没有什么独创性。他创作了不少长篇小说，但只有代表作《家》还算差强人意，其他长篇大都写得很烂，拖沓、臃肿、矫情，起码我在大学时代就读不下去。即便是《家》的差强人意，也绝非文学语言上的贡献，而是时代潮流使然。《家》是“青春骚动”的粗糙抒发，迎合了当时新青年的反叛情绪，但表达无克制，文字无美感，只能作为特定时代的社会学文本，但无法作为传之久远的文学经典，也就无法给后人以审美的滋养。<br />
<br />
所以，在文学独创性及其传承的意义上，巴金很难为后人提供创作的借鉴。后毛时代成名的中国作家在谈及自己的文学传承时，更多提到是西方现代作家的影响。至于中国现代作家，经常被提起的是鲁迅、老舍、沈从文，却很少有人提到巴、茅、冰的影响。但在权力至高无上和政治需要优先的当代中国，作家的成就和名声由权杖来钦定，只要党国需要，平庸的作家就会变成文学巨匠。当权力造就“名家”变成国人的习惯性思维之后，价值畸形和审美混乱也就成为必然，致使国人对精神产品的低下识别力延续至今。以至于，在巴金去世时的谄媚高潮中，有文人帮助党国排出了革命文坛的各代旗手：现代旗手是鲁迅，毛时代的旗手是郭沫若，后毛时代的旗手是巴金。<br />
<br />
六四以来，由于巴金的沉默让官权满意，他便被官权选为文坛“造神运动”的主角。进入新世纪以来，久病的巴金逐渐变成了说不出话、认不清人、手不能动、足不能抬、食不能进、排泄失禁的植物人，但植物人照样被缺席选为中国政协副主席。巴金过百岁生日时，党国赐以最高规格的隆重纪念和“人民作家”的最高荣誉，党国总理温家宝前往病榻前祝贺，北京、上海和成都三地同时举行庆祝活动，各地作协派人专程前往上海祝贺。<br />
<br />
2005年巴金辞世，党国要员和一大批文化名流高调称颂巴金，使文坛“造神”达到高潮。可怜的是，巴金提倡“说真话”，但总是在党国划定的范围内说真话，而绝不说过线的真话。比如，他只在被当局定性为“十年浩劫”的文革上讲真话，但在八十年代“清污”和“反自由化”等意识形态整肃运动中，他并没有讲真话；六四后的恐怖政治造成万马齐喑，是最需要说真话的时刻，也是巴金这样的名流最该说真话的时刻，但巴金却选择了“沉默是金”，直到他去世，整整沉默了十六年！<br />
<br />
恶心的是，文坛名流们参与“造神”，但他们并不打算响应“神”的召唤。他们纷纷赞美巴金的“说真话”和“自我忏悔”的精神，王蒙等文人更给巴金戴上“一面旗帜”和“世纪良知”的高帽，但他们并不打算用“说真话”和“忏悔”的行动来继承巴金的遗志。比如，余秋雨称：巴金“说真话”的遗训“最重要”，是“这个世纪箴言”；舒乙说：“《随想录》是个纪念碑。”然而，余秋雨对自己在文革中的表演，舒乙对自己在文革中批判亲爹老舍的大义灭亲之举，至今都毫无忏悔之意，更谈不上说真话了，居然还好意思大言不惭。所以，对巴金的无限拔高的悼念，不过是又一次犬儒大表演。<br />
<br />
最近，就在社会各界纷纷问责大地震凸显的中小学豆腐渣工程之时，余秋雨倒是敢为党国顶雷，说出了轰动一时的“真话”《含泪劝告请愿家长》。但他的含泪之劝，是在说党国想说而未敢说之言，与其说是感同身受的劝说，不如说是居高临下的指责；与其说是体恤灾民的动容，不如说是文化戏子的“鳄鱼泪”，其底色不过是奴才式的聪明。<br />
<br />
针对巴金去世时中国文坛的犬儒大表演，我的曾写过《巴金：一面下垂的白旗》，不妨在这里自我重复一下：以自然生命而论，在党国钦定的六大文坛“巨匠”中，巴金乃最长寿者。他在党国的精心护理下，活成中国文坛上第一位百岁泰斗。然而，以文学生命和人格生命而论，六位中最幸运的，倒是死得最早（1936年）的鲁迅，他过早的肉体终结成全了其精神长驻。他幸运地没有赶上“伟大的毛泽东时代”，不必在强制性的思想改造中挣扎，也不必没完没了地写自我羞辱的检查，更不可能遭遇文革式的羞辱。尽管，鲁迅被毛皇帝钦定了“骨头最硬”之旗手，变成残酷斗争中的打人棍子，但鲁迅的亡灵不该为毛泽东的罪恶负责。<br />
<br />
其他的五人，郭沫若从“流氓加才子”堕落成最无耻的文人，茅盾由小资加左倾活成圆滑平庸的文坛不倒翁，曹禺由戏剧天才变成懦弱的御用捧艮，老舍作为京味小说大师，先是被奉为“人民作家”，继而在党国无义和妻儿无情的夹攻之下，变成了太平湖里的鱼食；最长寿、也享受了最多官方优惠的巴金，由多产作家变成文学上的准植物人。<br />
<br />
在此意义上，韩寒仅从审美上说出“文笔很差”的评价，已经是很客气的态度了。<br />
<br />
