																											<div id="postmessage_326826" class="t_msgfont">“公民论坛”对话守则 ：<br />
<br />
1989年的冬天，哈维尔等捷克知识分子在布拉格成立了“公民论坛”，制定了8条《对话守则》，在街头巷尾、居住区、大学校园到处张贴，希望广为人知，以便监督。（1993年瓦茨拉夫・哈韦尔当选捷克第一任总统。瓦茨拉夫・哈韦尔出生于1936年，作为一名持不同政见者，长期从事反对前政府的活动，曾3次入狱。他是“七七宪章”和“公民论坛”的创始人。）内容是：<br />
<br />
1、对话的目的是寻求真理，不是为了斗争。<br />
<br />
2、不做人身攻击。<br />
<br />
3、保持主题。<br />
<br />
4、辩论时要用证据。<br />
<br />
5、不要坚持错误不改。<br />
<br />
6、要分清对话与只准自己讲话的区别。<br />
<br />
7、对话要有记录。<br />
<br />
8、尽量理解对方。<br />
<br />
?&nbsp; &nbsp; 我欣喜地看到，八卦办得越来越好了，读书的气氛越来越浓，理性的对话日渐丰富，攻击和谩骂日益减少，客观、公正、理性、宽容的风气发扬光大，百花齐放、百家争鸣的春天已经到来。<br />
??鲁迅先生说过：辱骂和恐吓决不是战斗。<br />
“在论坛上发帖跟帖本为一种表达观点、交流思想、锻炼写作能力的方式和途径，无所谓你存我走的论坛生存较量。道不合不相与谋，各不相干就行了。惜论坛终非净土，现实中的名利之争、道德冲突，在论坛反而愈发激烈。各色人等，互不相让，上演了一出出口舌之争的闹剧，个别风度欠佳气量狭小的甚至反目成仇，恶语相向。于是，就有了砸砖、掐架、干沉的说法。在论坛滥用心机打击异己实无必要。本羊一贯主张，论坛ID之间本为坛友关系，纵有矛盾，也不至于就达到你存我走不可同坛的境地。”（杂谈版友无法装狼的羊有一段话我非常赞赏）<br />
??观点不同，并不可怕，百家争鸣，才有思想的砥砺和见识的提高。动辄扣帽子、打棍子，一张嘴就粗野无礼，一副无赖腔调，“傻逼、婊子、操娘、狗日”等污言秽语漫天飞，并非就能证明自己的雄才大略，反倒露出了气急败坏和口不择言的破绽。<br />
&nbsp; &nbsp; 有矛盾并不可怕，矛盾也许会永远伴随着人类社会，重要的是我们面对矛盾的姿态，学会在对话、博弈中不断地解决矛盾，这个过程就是社会进步的过程。<br />
&nbsp; &nbsp; 这八条守则简单明了，而且很实用，值得人人学习，大力推广，特别是有一些守则恰好是我们习惯的思维方式中所缺乏的。无论是强势群体，还是弱势群体，归根到底都要学会对话，学会在对话中生存，而不是在仇恨和暴力中生活。学会对话，就是要告别自说自话，告别赢家通吃的意识，告别以强凌弱、强词夺理，当然也要告别仇恨意识、敌人意识。<br />
<br />
下面，是我对八卦的四个心愿。<br />
??一愿八卦网友，和心爱的朋友热情相拥，把人生的风风雨雨，化作绚丽的彩虹；读出好书的精彩，谈出心中的梦想；消弭仇恨，忘记悲伤，灿烂的星空下，你我的命运从此处起飞由黑变红，时时见笑容，事事皆成功；让真心的话语，和着开心的眼泪，在你我的心里交汇流动。<br />
??二愿八卦网友，用文字歌唱，让键盘舞蹈，把握好生命里的每一分钟；出产更多的精品，拥有更多的飘红。写出人间的真，歌颂心灵的善，描摹自然的美，抒发平凡而伟大的理想。手指轻敲，嬉笑怒骂随心所欲；眼波流转，任意播撒生命里的每一次感动。鞭挞丑恶驳斥谬论而不用污言秽语，言辞犀利入木三分而体现胸襟宽容；自由平等博爱和平，淋漓尽致虫要变龙。低吟浅唱，抚平内心的伤痛；仰天长啸，突显宽广的心胸。古今中外，穿越无垠的时空；土洋结合，随处可见真爱的影踪。不嘲笑弱者，不追捧权贵；不唯唯诺诺，不人云亦云；有自由精神，有独立思想；不仗势欺人，不献媚上峰。谁说民间无豪杰？伟大出自平凡中！数风流人物，请看八卦众英雄。<br />
??三愿杂八卦编辑，劳逸结合，养精蓄锐；傲立峰颠，挺剑苍穹。裁剪华丽精美的嫁衣，培育超凡脱俗的新娘；化腐朽为神奇，全靠伯乐们敏锐的目光。与作者共命运，同读者心连心；不因人废言，不拉帮结党。兼收并蓄，海纳百川有容乃大；兼容并包，百家争鸣万花齐放。版主们劳苦功高，是力挺杂谈的脊梁；默默地无私奉献，令人敬佩无上荣光。版主们牵线搭桥，越来越多的人群涌进八卦网。狼爱上了羊，少了一分暴戾习气添了一分温柔心肠；羊爱上了狼，消除了几分胆怯，增加了勇敢坚强。于无声处听春雷，八卦网友美名扬！<br />
