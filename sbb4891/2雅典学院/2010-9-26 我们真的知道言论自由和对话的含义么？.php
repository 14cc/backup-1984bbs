													<h2>我们真的知道言论自由和对话的含义么？</h2>
						
						
																											<div id="postmessage_845191" class="t_msgfont">我们这个论坛称之为1984 ，顾名思义是取之那部很有名的小说1984.说实在话，我没有看过，一眼都没有。可是，即使没有看过，我也从各个方面听说过，这是一部什么样的小说。这个论坛既然借鉴小说起了这个名字，并且在显著位置显示了“推崇布拉格公民论坛《对话守则》：对话的目的是寻求真理，不是为了斗争；不做人身攻击；保持主题；辩论时要用证据；要分清对话与只准自己讲话的区别；尽量理解对方。”这一条文，那么这个论坛的各位应该对言论自由有了深刻的理解吧。<br />
&nbsp; &nbsp; 可是，看看我们的论坛，充斥着“五毛”“轮子”（就差“美狗”了，原因估计就是论坛内美狗太多了）等身份的划定，而不是理性的辩驳！言论自由的含义是什么？我可以不同意你的意见，但誓死捍卫你发表自己意见的自由。我们的论坛呢？凡是稍稍有为裆、政、国说话的一律称之为五毛，然后就开始了对对方的智力加人身的贬低和对自身优越性的宣扬，恨不得贬的所有的五毛统统闭嘴，然后只留下一个声音，一个符合自己立场和意愿的声音。这是自由么？这不是，这是专制。因为只有专制者才把异见分子划为敌人，必欲杀之而后快。这种人虽然骂别人是五毛，其实自己才是真正的五毛，因为他自己正在身体力行，把五毛的思想有意识无意识用到自己的行动中去，他的危害，比被他划为五毛的人要严重得多。真正的言论自由者，不但不禁止对方发表观点，反而是鼓励对方发表意见，即使罪犯，再罪大恶极都有辩护的权利，这才是自由。<br />
&nbsp; &nbsp; 言论自由，还有一层意思，就是我由我自己的观点，但我同样尊重你的观点。谁都不是圣人，真理凭什么就在你那边？极有可能真理也在对方那边，为什么不能听听对方的话呢？对方说的很弱智很白痴，那是因为你聪明你知识广博，那么你为什么不能用您那聪明的大脑和渊博的学识来引导一下对方呢？引导不了您可以用您认为正确的逻辑和理论驳倒对方的观点啊。不试着去理解对方，不试着去和对方对话，反而上来就先对对方进行身份判定，看看是自己人还是异见分子，上来一句五毛就搞定一切了。只要认定了对方是五毛，即使对方说饿了要吃饭那也是错的了。你说对方五毛，对方说你美狗，你说对方轮子，对方骂你白痴，这样的话对于辩论，对于对话有什么用么？<br />
&nbsp; &nbsp; 我们在这个论坛内，正是为了寻找一方自由的空间，可是，这里自由正在逐渐的消失，原因就是有太多不知道怎么加进来没有自由精神却高举自由旗帜的人，从来没有看过对话守则，也从不试着接受一下论坛的精神，反而沉浸于把对方划为五毛 支那人来显示自己或肉体或精神叛离的优越性上。当这种人占据了优势，论坛也就成为了自由的废墟。<br />
<br />
&nbsp; &nbsp; 我只是在乱说。</div>

							
							
							
							
