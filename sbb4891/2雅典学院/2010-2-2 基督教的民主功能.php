													<h2>基督教的民主功能</h2>
						
						
																											<div id="postmessage_462425" class="t_msgfont"><img src="http://i892.photobucket.com/albums/ac121/wodetuchuang/Snap2-25.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
基督教是在众多民族和多元文化的背景下发展起来的。基督教内部在圣经的理解上常发生意见分歧。他们通过开普世的代表会议，通过讨论和表决，少数服从多数，形成统一的纲领和教义，这是一种民主机制，是一种协调基督教内部的分歧和维系基督教统一的方法。世界当代的民主制得益于基督教的普世会议可能多于希腊罗马的共和政体。新教的马丁&amp;＃8226；路德和加尔文强调，圣经的字字句句都是真理。他们认为，凭借每个人自己的信仰和上帝的恩惠，就能够认识上帝所启示的和耶稣所亲授的真理，这否定了教皇的代理作用；否定了少数教会领袖对圣经的解释权，提高了个人的自主性，为世界近代自由民主运动作了精神准备。基督教为民主提供了文化因子有：<br />
<br />
 <br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;1.人的原罪性和有限性学说，为权力制衡和法制体系的建立，提供了最初的思维诱导和最终的理论根据。 <br />
<br />
　　人有选择的自由。人可以听上帝的话，服从上帝的旨意，这意味着选择了善；也可听蛇的话，违背上帝的禁令，这意味着选择了恶。既然人有选择的自由，人就要承担选择的责任。人类的祖先亚当和夏娃最初听上帝的话，在伊甸园里过着幸福的生活；后来经不起引诱，听了蛇的话，犯了原罪而承受苦难。上帝因人犯罪而处罚人类，但上帝仍然爱人类。由于罪是要赎偿的，人本身没有能力来赎偿这个罪，所以上帝的独生子化身成人，以其在十字架上的死替人赎罪，克服人离弃上帝的罪况，使人与上帝重新修和。这就是耶稣基督拯救的意义。这种拯救是上帝白白施于人的恩典，所以被称为“救恩”。耶稣基督的救恩还意味着使人从罪恶的状态中警醒过来，决心做一个与上帝重新修和的新人。 加尔文神学认为，即使一个已经蒙恩得救的人，身上仍然有残余的罪性，今生今世难以达到完全。有限且有罪的个人所组成的任何政府都是有限的，也都受到罪的污染。每个政府都有各自的区域和界限，各司其职，互相监督。无论是个人、家庭、教会，还是国家，都不能扮演救世主（savior）的角色，只能在各自的领域中发挥仆人（servant）的角色。 人为罪人，这是基督教的基本观点。圣经里说的罪，是指人违抗上帝的旨意，妄想僭越上帝的地位以及行不义之事。人若将无限的价值加到自己人生的有限性中，就陷入到骄傲的罪性中。<br />
<br />
　　个人骄傲的罪有三类：权力的骄傲，知识的骄傲和德性的骄傲。<br />
<br />
<br />
　　（1）权力的骄傲，表现为人不承认自己的软弱，想通过攫取权力操控他人来克服、掩饰自己的软弱。<br />
<br />
　　（2）知识的骄傲，即理性的骄傲，它集中表现为人把自己有限的知识当成绝对真理。知识原本是某一方面有限的知识，却妄以为是终级的知识。知识的骄傲来自于人类不知自己的心智的有限性，来自于人欲掩饰知识的有限和知识所沾染的自私色彩。<br />
<br />
　　（3）德性的骄傲，乃是有限的人以他有限的德性为终极的义，以他有限的道德标准为绝对的标准。灵性的骄傲直接由道德的骄傲产生，表现为人将自己视为神明，将自己的偏私标准和有限成就当作无上的善，这是人的罪性的最后表达。团体的骄傲是个人的骄傲的集中表现，并且比个人的骄傲更加严重，它追求自私目标的行动更加不受限制，较个人更为专横，虚伪、自私和残酷。国家崇拜是团体的骄傲的最高峰。 <br />
<br />
　　正因为人的罪性，人人皆有犯罪作恶的可能，必须用制度来制约个人的权力，防止掌权者滥用权力。人如果不受限制，什么罪恶都能干得出来。历朝历代的皇帝、官员、警察、军队，由于自己就是法律制定者、执行者，故作恶多端、丧尽仁义天理。而那些有信仰的国家的首脑、官吏，由于知道有个高于自己的上帝，由于坚信来世会受到审判和惩罚，即使他们拥有作恶之后不受现实惩罚的权力，他们也不逾越规矩，不为一己之私丧尽天良。1992年1月美国副总统奎尔说：“世界历史上的专制暴政，并非人类不幸的根本原因。专制暴政有人类灵性上的根源，这就是不承认人的有限性，不承认人人都有原罪。哪里的人们把自己的命运和前途完全建立在对人的信赖之上，而没有超越个人、政党及其‘主义’之上的信仰作为文化的、心理的基础，哪里就难免滋生专制暴政。” <br />
<br />
　　保罗，蒂利希说，人的生存勇气首先是“接受自己的有限性的勇气”。正因为人的有限，人应该谦逊而不独断；人应忏悔而不是指责；应承担责任而不是推卸责任。正是承认每个人的有限、无知这一事实，社会才需要自由与宽容。而长期受到“人定胜天”教育的中国人，很难认识到自身的有限性，很难从狂妄的迷雾中脱离出来。没有“原罪”，便没有民主。对人的罪性和有限性不醒悟，是专制的灵根；意识到人的罪性和有限性，则是民主的起源。 <br />
<br />
　　《圣经》里有个故事：有妇人犯通奸罪，依摩西的法律当乱石砸死。法利赛人把这案子交给耶稣裁决。耶稣说，你们中间谁是没有罪的，谁就可以先拿石头砸她。人们听了这话，从老到少一个一个都离去了。结果，没有一个人敢把手中的石头砸向这位妇人，耶稣放了她。假如那人群中掺杂着一个中国人，这妇人就遭殃了，准有一块石头击中她的命门，叫她一命呜呼。中国人不承认人人有罪，只承认“个别人是没有罪的”。为证明自己没罪，最直接的办法就是诬陷别人。仇恨、争斗就难以避免。愈窝里斗，愈发罪孽深重。最终有一个人会让天下人知道他是最清白、最高尚的，此人就是皇帝。自古皇帝加尊号，可以用十几个最好的词藻，九五至尊以自我神化为乐。此等教化下，普通百姓也无自我反省之心。一块石头，在《圣经》里是检验人皆有罪的试金石，在中国却进入了一个很不光彩的成语：“落井下石”。 <br />
<br />
　　神才是人类道德、法律的源泉和根基。公元890年，阿尔弗烈德(Alfred)国王――英国惟一伟大的君王，他把摩西十诫及耶稣的金律做为英国法律规条的基础。1215 年总主教兰顿(Stephen Langton)使用以圣经为基础的英国法律，拟成了英国《大宪章》。文件一开始就说:“恭承神恩，英格兰国王……”由此可见基督教因素对人权、自由的重大影响。伯尔曼在《法律与革命》一书中表明，没有教会法，没有一连串的宗教改革，现代西方法文化的基本质素是不可想象的。“没有信仰的法律将退化为僵死的教条”，“而没有法律的信仰……将蜕变为狂信”。宗教是信任的根基，是法律产生的根基。由于法律来自上帝（摩西十戒），因此法律高于国王。国王犯罪也应推上断头台，总统有错也遭弹劾。任何人都不能在法律之上，只能在法律之下。人们在法庭作证都要手按《圣经》发誓，这是借助于上帝不许说谎的命令，迫使人们讲真话。因为上帝是全知全能的，它知道过去、现在和未来；它不仅能知人们做了什么，而且更知人们想什么。所以，在上帝面前讲假话是逃不过末日审判的。西方民族具有好讼精神。为一、二元钱的琐事打官司常见诸报纸，这种好诉精神来源于基督教的平等正义，凡事讨个说法、弄个明白。正是这种精神推动了世界法治文明的进程。<br />
<br />
 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 2.上帝按照它的形象造人的说法为人奠立了极高的地位，极大地提升了人的主体性，使人意识到自己的尊严、自由和平等，这种意识是民主社会不可或缺的心理基础。 <br />
<br />
　　《圣经》教导我们，人与人之间应当是平等的关系。“若有别害，就要以命偿命，以眼还眼，以牙还牙，以手还手，以脚还脚，以烙还烙，以伤还伤，以打还打”(出埃及记21：24)。这段教导的精义是要求人们要限制报复的程度，保持对等原则，报复不能超过原罪行造成的伤害。大约在公元前1750年的巴比伦“汉摩拉比法典”里也有这样的记载，“一个人如果毁掉别人的眼，则应以毁掉他自己的眼为惩罚”。上帝颁布律法，用“以眼还眼，以牙还牙”这有限的报复来约束无休止的杀戮，是对没有反抗能力的弱者的保护，这正是体现了上帝的大爱。除“以命还命”的罪外，都可用赎价去替代。“子不必担当父亲的罪孽，父亲也不担当儿子罪孽。义人的善果必归自己，恶人的恶报也必归自己”（以西结书18：20）。相比之下，中国一人犯罪、株连九族的连座制则是多么惨无人道！“人若死了，没有儿子就要把他的产业归给他的女儿”（民数记27：8）。“我对着没有嫁娶的寡妇说：若他自己禁止不住，就可以嫁娶；与其欲火攻心，倒不如嫁娶为妙”（歌林多前书7：8）。《圣经》中上帝借他的使徒之口给人类这种人性上的自由、平等与尊重和妇女解放的教导，比起儒家倡导的妇女从一而终、女人只能守寡、男人却能三妻四妾的婚姻奴役，不知文明多少倍！看看皇帝刘邦的诗：“青竹蛇儿口，黄蜂尾上针，我若不是妇人生，天下妇人都杀尽。”<br />
<br />
<br />
　　基督教第一次在人类历史上建立起所有的人一律平等的观念。基督教主张信奉基督的人都是兄弟姐妹，都是上帝的臣民；无论是国王还是平民，无论是奴隶还是公民都处于主的恩惠之下。基督教每一座乡村教堂的基本格局、基本功能、崇拜礼仪和崇拜对象，都同王公贵族甚至皇帝宫内的教堂完全一样。罗马君士坦丁大帝接受了基督教，并参加崇拜仪式。有一次，他的随员发现有一位讨饭的也和君士坦丁大帝一起崇拜，认为高贵的皇帝不应该和低贱的人在一起崇拜。君士坦丁回答说，在上帝面前人与人没有贵贱之分。普通的基督徒，也是同王公贵族处于同等地位的。一个下等阶层的基督徒，完全可因其信仰虔诚而得到比堕落腐化的王公贵族更高的尊敬。反之，神职人员、王公贵族甚至皇帝本人，都被视为带有原罪的人，在宗教方面是无法趾高气扬或享受特权的。著名的卡诺莎城堡事件就是一个明证。<br />
<br />
<br />
　　为什么人人平等的观念只有在神权关系中才能确立？因为：人是有限的，人与人之间不可能在自身之内求得平等的对待。贺卫方先生最近慨叹：中国的法院越来越成为迫害人权的阵地；法院里的司法伦理已经跌破了底线。跌破底线是由于人心中的正义缺乏一种超验的支持。所以要重建伦理的话，必须使正义有一个更高的来源，超越于人本身。只有在人之外设定一个超越的第三者，人与人之间的平等权利、社会的正义才能确定起来。这个第三者既然是与人有区别，不是上帝又是什么呢？<br />
<br />
<br />
　　无神论者和经验主义者不承认有普遍的人权，就从实质上否定了人权。一个无神论者当他要求别人尊重他的生命权利如人身自由、人格尊严时，却没有任何正当理由。你既然相信人性本善，相信人不过是动物中的一种而已，相信动物社会的法则是弱肉强食、适者生存、优胜劣汰，那么当你面临别人的欺凌压迫时，按无神论的人生观理当逆来顺受，你有何权利抱怨呢？现实生活中，人们并不能安然接受别人的欺凌[象刘少奇]。这是无神论者无法自园其说的哲学难题。当我们否认上帝的时候，我们就否认了生命的价值、权利、意义和尊严，不可避免地要沦落到动物界血腥专制的奴才境地。<br />
<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;3.上帝作为全能的主宰和绝对的正义，在人心中是超越一切的权威。<br />
<br />
　　当人的良知直接与之沟通时，人就获得了独立评判人世间一切事情的勇气和准则。这时，人具有超越性。 《圣经》里有个故事：美丽善良的姑娘苏姗娜遭人诬陷，一个名叫但以理的青年在上帝的启示下，凭借正义和智慧解脱了苏姗娜，并使恶人受到了惩罚。这一案例以上帝的名义昭示了两种司法精神：一是正义精神，坚信上帝的天启“切不可将无辜者处死”，所以他能够以无畏的精神主持公道，惩恶扬善。二是理性的精神，但以理不是简单地凭一种信念为无辜者辩护而是以超众的智慧，采用将两名证人分别询问具体情节的方法，最终使诬陷者漏洞百出。可见伸张正义不仅需要一种无畏的精神，更需要有一种过人的理性。<br />
<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;4.公平的规则要满足模糊面纱的原则。<br />
<br />
　　模糊面纱的原则是说，制定游戏规则的人在制定规则时，不能把自己的利益最大化，要照顾对手的利益。这是正义秩序的起码的要求。怎样才能做到模糊面纱的原则呢？第一，你要有爱敌人的意识；第二；你要有害怕下地狱的恐惧。只有信教的人才会做到。信仰上帝能树立一种谦卑，人就会自觉地约束自己[如华盛顿]。这样外在的民主监督加上内在的自觉就会使一个制度走上良性运行的轨道。<br />
<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 5.领导就是服务、人民至上的观念。<br />
<br />
　　耶稣为门徒洗脚，他说“谁愿为大，就必作你们的佣人，谁愿为首，就必作你们的仆人，正如人子来，不是要受人的服事，乃是要服事人，并且要舍命，作多人的赎价”。不但没有一点作威作福的意思，反过来，是要为天下的人舍命（《马可》第10章43～44）。在这里，耶稣规定了领导就是服务，同时也等于间接否定了一切以暴力手段把持领导权以谋求一家一党一派利益的丑恶现象。民主政治对领导人的要求是：领导人勇于或乐于辞职，不应当有过分的权力欲，而只应忠实地为神为民工作；领导人应当具有诚信的素质，使他具有实现选民意志的能力。“上帝选民”并不意味着自命不凡，自觉高人一等，颐指气使、盛气凌人地对待别人；相反只意味着应谦卑柔和地实行上帝之家，服侍帮助他人，在世间努力惠及每一个最小的“小子”。在耶稣心目中，普通的人民，是人类活动最高的目标，他说：“你们中间最小的，他便为大”（《路加》第9章48节)。人民至上，领导就是服务是耶稣为人类幸福确立的至高无上的政治原则。 <br />
<br />
　　中世纪最早提出民主的是加尔文教派；而最早推翻暴君的是路德教派的人。1989年的大变革还证实了基督教的民主功能。在波兰，一个虔诚信教的造船厂电工瓦文萨竟成了自由工人运动的核心，率先瓦解了一党专政。在过去的几十年里，东德不少教区牧师把教堂向市民敞开，为他们的抗议活动、为有思想的年轻人上演戏剧和创作诗歌提供了讲坛和场所，直到后来教堂里无法容纳那么多人为止。在成千上万的东德人选择去西方之后，他们涌上街头，在德国历史上发动了第一次不流血的革命。一个正派的、关心他人但又不热衷于政治的蒂米什瓦拉新教牧师挺身而出抵制其苟且偷安的主教，结果无意中激发了一场革命，导致了罗马尼亚共党暴君的倒台。所以，没有坚定的宗教信仰及其教会组织，实行民主是极其困难的。 儒教的基本功能呢?直到现在仍是靠暴力的轮回,专制的说教与教化来建立臣民,还能持续多久呢?把历史之钟拨快点,其变即显.朱世筹.<br />
