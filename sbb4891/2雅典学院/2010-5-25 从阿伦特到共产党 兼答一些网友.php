													<h2>从阿伦特到共产党 兼答一些网友</h2>
						
						
																											<div id="postmessage_638063" class="t_msgfont">徐贲是我很喜欢的一个学者，阿伦特更是见解独到、深刻，提出了很多政治哲学原创性的论断。我的很多观点都受益这两位的启发，说崇拜他们也不为过。<br />
<br />
这次在关于“共产党是不是邪恶组织”的讨论中，我提出来否定的观点，抑扬转载了徐贲的这篇文章，我就从徐贲的这篇文章展开来说我的理由。徐贲文章参见：<br />
<a href="https://1984bbs.com/viewthread.php?tid=46842&amp;extra=page%3D2" target="_blank">https://1984bbs.com/viewthread.php?tid=46842&amp;extra=page%3D2</a><br />
<br />
在我看来，阿伦特关于平庸邪恶的发掘和剖析，既是她只是惨痛经历的直接反应，更是一名政治哲学家敏锐而深刻的洞见。我认为她对平庸邪恶的分析与她提出极权主义的概念有很强的内在联系，平庸邪恶是极权主义的重要基础。极权主义固然有理论上理想主义的缺陷，发动者和主导者本身对权力的渴求和滥用，也有大众平庸邪恶的特质作为支撑。<br />
<br />
但是我们应该注意到，阿伦特并没有对平庸邪恶做受体的区分，也就是没有在人群中说那个组织，或者说从事哪项活动的人属于平庸邪恶，另外的人则不属于。这是我们思考共产党作为组织的一个关键出发点。不是说党员或者说申请入党的人具有平庸邪恶，而其他人除外。只有盲从者容易陷入平庸邪恶中，而独立思考、恪守良知的人容易摆脱。在极权统治之下，有人深刻认识到极权主义政治结构给社会带来的危害，同时放弃一些现实利益来捍卫自己对良知的坚守，他们当然是引领我们摆脱平庸邪恶、可尊敬的先行者。但是同时符合这两个条件的人毕竟少之又少。更多的非党员、不入党者仅仅是因为一种漠然，利益过于微小，不能简单因此说他们也摆脱了平庸邪恶。<br />
<br />
上面这一段并没有正面回答抑扬的问题，而且从某些角度看，似乎支持了抑扬的观点，因为分析的基础是：共产党当局对社会的负面作用远远大于正面作用。但是这并不代表共产党本身就是邪恶组织，共产党的发展壮大演进掌权是一个复杂的过程，它不过是极权主义理论胜利的承载者。只要我们到了极权主义胜利的稳定期，无论这个当权者叫做XX,XXX或者共产党，都以及没有太大的意义。我们能够做的仅仅是让更多的人认识到极权主义给社会带来的危害，找出一条路径让整个社会摆脱这个危害。而摆脱这个危害主要就两种方案，激进和温和。<br />
<br />
激进的期待迅速改朝换代，直接把当局定为“邪恶”符合他们的行动纲领和路径。温和的正好相反，他们认为单纯地激化矛盾弊大于利。激进者最站得住的理由是可以迅速摆脱现在的苦难，而最站不住的弱点就是动荡后未知的未来。而温和者正好相反，他们思考和行动的出发点都是动荡后的未来一定优于现实，但是他们的弱点是无法承诺革命的发生，没有后者前者仅仅是空想。<br />
<br />
激进者和温和者看似有矛盾冲突，其实并非如此。他们既可能互补，也可能对抗，这取决于双方的策略。他们的理论和实践都有巨大的缺失，如果用甲方的优势填补乙方的缺失，那么就形成了一个综合双方优势的力量，反之，如果用甲方的劣势来冲击乙方的优势，整体正面的力量将远远小于负面力量。<br />
<br />
互补和对抗是不是很难确定和操作？其实很简单，只要双方不要把矛头指向对方，就是互补；而把矛头指向对方，就会形成对抗。激进者没有必要去攻击温和者立场，温和者也无需劝说激进者放弃革命。实际上，这种攻击和劝说往往没有任何效力，他同每个人的思想背景、现实际遇有关。如果我们能够不形成内耗，就是互补了。<br />
<br />
要我们在“共产党是否邪恶”这个问题上表态，仅仅是区分激进和温和的一种粗略方式，这种方式没有实质意义。只会让激进者会因此有种道德成就感。有意思的是，极权主义组织在没有取得权力之前，更多地也是靠道德感来团结队伍，而且因为这种道德感投身组织甚至献身的人，往往是真正的道德高尚者。组织本身的缺陷与参与者的高尚可以共存。<br />
<br />
<br />
最后声明一点，温和者并不是犬儒的同义词，温和者也渴望变革，而且是更深层面，比如文化的变革。以温和的名义抱残守缺、安于现状的人是现实的合作者，不是温和的变革推动者。关于温和的变革推动可以展开说的还有很多，这里打住。</div>

							
							
							
							
