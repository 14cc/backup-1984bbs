&nbsp; &nbsp;&nbsp; &nbsp;3关于民-主-派人士在F-L-G受迫害问题上究竟应该持何态度的问题。这个问题本来不应成为一个问题，但是就像很多问题一样，不成为问题的问题在当今中国仍是扰不过去的问题。如果当局迫-害的是盲人，贫民，流浪大学生，那么为他们大声疾呼是不成为问题的。可是如果当局迫害的是“不科学不民主”的邪-教（当局语）或嫌疑邪-教，那么是否有必要站出来大声疾呼，就要犯个嘀咕了。并且还要核算一下“成本”。<br />
