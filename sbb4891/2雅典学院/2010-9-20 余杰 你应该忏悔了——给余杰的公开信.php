													<h2>余杰      你应该忏悔了――给余杰的公开信</h2>
						
						
																											<div id="postmessage_837843" class="t_msgfont">荆楚<br />
<br />
说实话，过去多年，我对余杰的才气，是比较欣赏的。对余杰作为一名在校的大学生，就写出了不少有深度的作品，也是敬重的。对其接二连三地出版的不少书籍，只要在书店里看到，我一般买回来阅读并收藏的――这是出于我对作者的信任，并感叹――后生可畏。只是对其过于繁琐的部分文本，有些看法。也对其推崇武侠小说，不以为然。<br />
<br />
但总的说来，这些看法或不以为然，属于各人的见仁见智，尚不足以减损我对余杰的欣赏。因为我素来视才智之士为我主对人类文明的恩赐。因而注意自觉避免中国文化“文人相轻”的传统。<br />
<br />
随着“余王拒郭”事件的展开和揭露（王怡在这件事情上比较低调，其行止也很少被披露出来，且我对王怡的了解也很有限，就不妄置一词啦），余杰面对香港教会的抖出真相和真诚批评，不但不思忏悔，反而是制造更大的错误，来掩盖他先前的错误――对失去自由和话语权的人，竟然散布流言蜚语。对被他伤害过的对象，进行诋毁和攻击。企图以此来转移视线，来证明他“一贯正确”。<br />
<br />
这样一来，他倒是活学活用了他极力批判的“毛思想”，从而陷入了他极力否定的“文革流毒”之中去了。<br />
<br />
面对这一现象，我老是在心里犯迷糊：作为一个基督徒，最基本的品德要求是诚实。而余杰面对香港教友的批评，竟然使用了他一直声讨的“官本位”价值符号来纠缠。说该教会“在大陆，只不过属科级架构”。他的这种做派，对于一个口口声声宣称自己是一个虔诚的基督徒来说，实在让人大跌眼镜！<br />
<br />
这样的一来二去，我对余杰的敬佩和欣赏，就减去了九分……但到了此时，我仍然保持缄默。因为我素来奉行与人为善，忧虑于国内外民主力量的内讧不断，从而减损了中国文明进步的推动力，使得中国民主运动陷入低迷徘徊之中。<br />
<br />
由于我在职时为文“闯祸”，并被迫“买断工龄”失了业之后，除去不忍心拂了难兄难弟难姊妹们的信任，而带着他们从事了一年有余的维权事务之後，就一直沉湎于阅读、思考和写作。我将这些这些文字在海内外陆续发表出来后，从而使我结识了文坛上的不少朋友。从而使我知道文坛诸多人对“余王拒郭”事件的愤愤然。也对余杰这件事情鄙之小之……<br />
<br />
到了这时，我仍然保持缄默。因为我不想陷入口水大战之中，而愿用更多的精力去思考一些有益的东西。<br />
<br />
昨天（11月19日）突然出现的一件事情，对我产生了强烈的刺激。促使我改变初衷，决定写这封公开信了。<br />
<br />
事情的原委是这样的：<br />
<br />
由于我的《也论中国人的病态人格》一文受到了不少读者的错爱，被转贴到许多地方。也因为国内BBS上言论空间狭窄，许多网站BBS只敢节录刊载。不少读者纷纷通过即时通讯工具找到我，要求传送一篇完整的文本，“一睹为快”。<br />
<br />
自从我被《凯迪》老网友劝回《凯迪》之后，由于《凯迪&#8226;猫眼看人》不许我贴出完整的《也论》一文，而只能贴出节录。节录的文本贴出后，于是不少凯迪读者批评我说：故意不贴完整的文本……我只好承诺：只要找上门来，我有求必应。<br />
<br />
节录文本贴出一个星期，浏览量就达到四万多人，并回帖一千多条（后来因为讨论热烈，被《凯迪》背后那只强权的手删去了）。承诺“有求必应”后，向我索要完整文本的网友纷至沓来，高峰时，每天传送一百来次。低谷时，也有几十遍。遂我被不少凯迪网友说成是“猫王”了。<br />
<br />
有一位令我敬重的年齿稍长的网络作家亦忱先生，他在另一个BBS上看了我的这篇文稿之后，毫不客气地批评我说：“看到你在文中推崇余杰，就像正吃着一碗美味，却突然吞下了一只苍蝇……”<br />
<br />
我只好对他解释说：“这篇《也论》，是在职时就开始写，并断断续续写了八年多，一直写到去年才杀青。因此，对于那些早先形成的文字，虽然今天的观点已有很大的改变，但为了保存自己的思想轨迹，固没有改动。以后有机会公开出版时，再修订过来……”<br />
<br />
看到这种状况，连我都感到吃惊不已――本来文名雀起的余杰，竟然被“糟蹋”成了这步田地！我也为他感到惋惜和痛心。所以就仿照他《余秋雨，你为什么不忏悔？》的笔调，写了这篇《余杰，你应该忏悔了！》的短文。（所谓以子之矛攻子之盾也）<br />
<br />
请各位朋友明鉴，我的目的不是为了损他的。乃是期待他能在我主的面前忏悔改过。因为人非圣贤，孰能无过？<br />
<br />
他如果能这样做，不但无损于他的成就，反而会赢得人们的敬重的。否则，会而让人对之吐唾沫的。独立作家笔会的众多成员，也会因为余杰而蒙尘含垢的。<br />
<br />
首发于（自由圣火<a href="http://www.fireofliberty.org/article/1634.asp" target="_blank">http://www.fireofliberty.org/article/1634.asp</a>）<br />
<br />
（写于民国95年11月20日）<br />
<br />
以下是引用《自由中国BBS》网友的批评和回复：<br />
<br />
茕茕白兔：从福音书的记载看，耶稣肯定讨厌余杰、余秋雨、荆楚这样的人――假冒为善的人、做作的人。<br />
<br />
“写于民国95年11月20日”。矫情无聊莫过于此。<br />
<br />
荆楚：民国纪年是历史上的客观存在，且今天仍然在延续着。矫情何有？无聊何在？<br />
<br />
我在内心深处认同中华民国的法统，而视中华人民伪共和国是非法的。这应该是我个人的情感。茕茕白兔君当然可以不同意，但你没有权利用“无聊矫情”来指责！<br />
