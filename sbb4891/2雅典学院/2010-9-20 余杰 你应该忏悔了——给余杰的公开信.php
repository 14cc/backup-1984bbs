																											<div id="postmessage_838653" class="t_msgfont">&lt;我对余王拒郭事件的态度&gt;<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;余-杰王-怡在美国对傅-希-秋说,他们不愿与郭一同见布什.如果郭去见,则他俩退出.结果傅最后决定,并征得白宫同意,余-杰王-怡参加,郭退出.<br />
&nbsp; &nbsp;&nbsp; &nbsp;以上是不带任何感情色彩的陈述.当然这事也可以有不同的叙述方式,论坛上很多,我就不一一列举.<br />
&nbsp; &nbsp;&nbsp; &nbsp;很多人指责余杰王怡通过逼迫的手段，把郭-飞-雄排除在会见之外。在这件事上，如果用“逼迫”这个词，那么即是指余-杰-王-怡的道德状况出了问题。无论古今中外，在多数时候，如果道德出了问题，那么接下来，对于道德亏欠者，就没有什么好说的了。<br />
&nbsp; &nbsp;&nbsp; &nbsp; 但是张-国-堂先生说，在这个事情上，余-杰-王-怡并没有逼迫谁。余-杰-王-怡有权退出。余-杰-王-怡的意思是，决不一起出席. 如果他们出席，则希望郭退出，如果郭出席，则他们推出。这是他们的选择，他们的决定。当然他们有这个权利做出自己的选择和决定。这种辩解没有任何缺陷。<br />
&nbsp; &nbsp;&nbsp; &nbsp;但是另一方马上就提出一个事实。这就是，余-杰王-怡是多数派，郭-飞-雄是少数派，当然如果他们退出，就只剩下一人参加。在这种情况下，傅-希-秋很难有别的选择。虽然余-杰王-怡没有拿枪指着傅-希-秋，但是并不是只有拿枪指着才叫“逼迫”。余-杰-王-怡的做法置傅-希-秋于很难有别的选择的境地，就是一种“逼迫”。这种指责同样没有任何缺陷。<br />
&nbsp; &nbsp;&nbsp; &nbsp;在这种指责的基础上，论者就指出：如果高/范成行，王-怡没有到美国，余-杰这时候成为少数派，则他会提出自己退出会见么？提出这个问题的人显然是说，肯定不会。但是这个问题并没有答案，有的只是猜测。<br />
&nbsp; &nbsp;&nbsp; &nbsp;如果人多时提出退出会见（事实），人少时不提出（猜测），则余-杰的道德就真成了问题了。这在论坛里好象成为一个定论。于是卑鄙说/权谋说就泛滥论坛。<br />
&nbsp; &nbsp;&nbsp; &nbsp;涉及这个事件的，还有许多疑问之点。其中之一是：余-杰王-怡为什么不愿与郭-飞-雄一起出席？<br />
&nbsp; &nbsp;&nbsp; &nbsp;按照卑鄙说/权谋说的说法，余-杰王-怡无非是争权夺利，不惜以打击别人来抬高抬高自己，余-杰-王怡的辩解，经过分析，无非是巧言令色的托词。按照这些人的认识，民-运从来都是一个争名夺利的大市场，这种现象也不会因为出了个余-杰或是高-智-晟或是别的什么人而有所改变，相反，余-杰正好做了一个很好的注脚。按照这种世界观，那么余-杰-王-怡注定在劫难逃。<br />
&nbsp; &nbsp;&nbsp; &nbsp;但是这不是我的世界观。所以在我来说，要全盘了解事实真相，有必要认真读读当事人余杰王怡的申明。根据当事人余王的申明，余-杰-王-怡拒绝与郭-飞-雄同时出席的根本原因在于：政见不同。究竟有何不同，语焉不详，但是据透漏大约有两端：一，郭-飞-雄在赴美之前好象知道可能会见布什，但是没有告诉余-杰-王-怡，二人结合以往对郭-飞-雄的看法，判断郭-飞-雄有将基督教维-权-运-动政-治化的企图，而这一点，在余-杰-王-怡看来是不可接受，不可容忍的。二，余杰王怡不能同意郭-飞-雄的“反美言论”，不能同意郭-飞-雄将要在会见中提出F-L-G问题的做法，并决心同这些做法彻底脱离干系，“洗清”自己。<br />
&nbsp; &nbsp;&nbsp; &nbsp;余-杰王-怡的申明（或曰道歉）又引出更多的问题，引起了激烈的讨论。非-政-治-化-维-权与政-治-化-维-权究竟有何恩怨情仇？纷纷扰扰？究竟孰是孰非？信仰普世价值的民主派人士在F-L-G受迫害问题上究竟应该持何态度？在民族国家与国际关系问题上究竟应该持何态度？<br />
&nbsp; &nbsp;&nbsp; &nbsp;我认为，余-杰-王-怡的申明认真地提出了一些重要的问题，完全可以回应来自道德的非难。所有这些问题都急需要认真讨论，认真澄清。但是认真讨论这些问题超出了论坛一二搅浑派人士的愿望和能力。我在此讨论余王拒郭事件，对此亦不做展开，因为我认为，避开这些难于说清楚的棘手的问题，仍然可以清楚提出我对余王拒郭事件的思考和认识。我做如下几点分析：<br />
&nbsp; &nbsp;&nbsp; &nbsp;1假设郭-飞-雄提前得知可能会见布什而没有提前告诉余-杰-王-怡这个情况，是否可以认定是郭-飞-雄的错误或处置不当？不能。因为会见并没有确定。并且郭飞雄也可能认为余-杰-王-怡了解这个可能之事。一切都在可与不可之间。所以余-杰-王-怡如果仅凭这一点来认定有计谋和手段，是不妥当的。<br />
&nbsp; &nbsp;&nbsp; &nbsp;2关于维-权-运-动政-治-化。按余-杰-王-怡的申明，这是他们最担心的问题。当然我认为，对于基-督-教-维-权-运动（不是其他维-权-运-动）政-治-化的担心，是正当的。并且我认为是必要的。余-杰-王-怡认为，基-督-教-维-权-运动应该避免政-治-化，这是出于几个方面的考虑：一、教会应自觉和政治分离，对政治应有适当的距离，而作为基督徒，在基督-教维-权-运-动中政治化基督-教维-权-运-动，则使人怀疑。这一点余-杰-王-怡分析的很清楚。我认为这是实质之点。二、从策略和方法上说，政治化基督教维-权-运-动不利于基督教维-权争取更多非政-治化人群的同情，不利于扩大基督教-维-权的力量。这是非实质之点。<br />
