																											<div id="postmessage_302246" class="t_msgfont">我谈芦笛先生<br />
刘松萝<br />
<br />
1.<br />
我上网发表文章晚，是在2004年。说得具体一些，是在2004年10月12日晚上，在凯迪发表了《走出象牙之塔与走进象牙之塔――探讨大学在市场经济中的地位》。这么晚发表文章，是不愿意打乱生活的安宁。说得直接一些，就是胆子小。<br />
<br />
因此，我说过，把那些在此之前就活跃在网上并且有一些影响的人一概视为先驱者，不批评他们的文章。假如发生争论，一定会退避三舍。争论没有发生过，我想是因为轮不到我去争论。<br />
<br />
我对芦笛先生最早的印象，是网络四大汉奸之一。另外三大汉奸，是赵无眠、林思云和马悲鸣。汉奸的说法，当然是不能够成立的。至于马悲鸣先生，简直就是超级爱国者了。<br />
<br />
以后，赵无眠先生和林思云先生的思路有些狭窄。特别是林思云先生，掌握的材料虽然多，一心作反面文章，有些钻牛角尖。相比之下，芦笛先生就不一样了。他的眼界开阔，视角独特。给人留下深刻印象的，是他在文章中展现出来的才气。<br />
<br />
2.<br />
读芦笛谈论宋庆龄女士的文章，感受非同一般。他把历史问题写得轻松活泼，充满了调侃而又不失严谨。文章的内容现在仍属敏感，大家只能自己去找了。此文一开头，就把汉奸当做笑话：“昨天，林奸思云说，许多女人爱革命家，所以革命家们三妻四妾，也是双方的责任，未可单责一方，此话有一定道理。”谈到孙中山先生原配的芦夫人，芦笛又说：“今天大贼向我报告好消息，说咱们芦家原来有位不得了、了不得的孙姑爷（姓孙的姑爷，不是孙女婿）。”当然，只能说总体上不失严谨，根据道听途说所做的猜测也是有的。<br />
<br />
芦笛的散文，不但旁征博引，而且很多人们以为平常的事情，以为并不相关的事情也被引用。记得在文革就要开始之前，有一部批判当时的教育制度的话剧叫《一百分不算满分》。在剧中，一位充当反面教员的校长老师用塑料普通话告诉学生怎样去写散文：“那个散文，顾名思义就是要散。一篇好的散文，要思路开阔，要天马行空，最后还要回到主题上面来。”好长时间过去了，我认为符合这一标准的散文非芦文莫属。<br />
<br />
3.<br />
时光流逝，物是人非。有人说，往日的四大汉奸，现在不过是网虫而已。我也听说，包括芦笛在内的海外知名网上人物混战了一场，其状有些不堪。<br />
<br />
我没有看到混战，看到的是芦笛先生海归了――把文章贴到国内的网站。现在看来，这是一个正确的选择。芦笛先生海归之初，我给他发去邮件，对先驱者表达了敬意，还附上了我的博客。他的回复礼貌而又简单，这自然是名士的做派了。<br />
<br />
岁月把芦笛变成了一位忠厚的长者，一位虽然有些严厉但诲人不倦的老师。他耐心地写，一篇一篇地贴出来。应该说，很多朋友对他的印象，来自他的新作。个别的时候，芦笛的个性还是能够表现出来。在批判鲁迅的文章之后，芦笛在跟帖中说：“要论小说，我写不过鲁爷。要论思想，嘿嘿！”<br />
<br />
根据我的经验，勤奋写作的人不一定很辛苦，写作是一种快乐。能够经常有所发现，能够把想法写下来，并且能够得到一定的认可，这是天底下少有的幸福。<br />
<br />
4.<br />
我们虽然三句话不离改革，其实骨子里面是很没有创造性的。如今，那些浪得虚名的人，那些本应该过气的名人，捧上了铁饭碗，坐上了铁交椅。不少写字的人，整天就在论证老名人那些语无伦次的胡话如何有道理。实在是理屈词穷的时候，他们还可以说名人的话是反讽。<br />
<br />
芦笛先生应该算名人了。倘若芦笛也去说套话，去沿着旧有的思路不断重复，或者放开了骂人，也会有不少的支持者。<br />
<br />
芦笛的文章基本上是有感而发，有针对性，没有被以往的成就所局限。<br />
<br />
谈到基层的官民冲突，芦笛认为，古代的中国是一个有皇帝，有官员，有百姓的三明治社会结构。现今的中国，则是独一无二的官民对立的二元结构，更加缺少制约。<br />
<br />
芦笛讲述英美在翻译外国名称时的随意和固执，也很有趣。<br />
<br />
还有，刚刚看到一篇芦笛的《野蛮的俄罗斯“反世界”》。其中的观点不论，有一句话很精彩：<br />
<br />
网友问我，什么是文明？我早就说过了，文明就是远离丛林，就是扬弃兽性。所谓文明，说到底不过是一堆做人行事治国的规矩。人类要缔结成可以和谐运转的社会，就必须压制人类的兽性，遵守一整套为全社会公认的规矩，这就叫体面，这就叫文明。不管社会处于何种状态，是奴隶社会也好，封建社会也好，资本主义社会也好，只要是人类社会，就一定有共通的永恒的行为准则，例如诚实、守信、廉洁、慈悲、仁爱、顾全大局等等。无论时代怎么变化，它们都是被人类普遍肯定的基本文明原则，都是做人做事从政的起码规矩。蔑视践踏抛弃这些全人类承认并行之几千年的规矩，就必然回归兽性，建立起一个魔鬼世界来，而这就是十月革命的光辉道路。<br />
<br />
我的跟帖是：<br />
<br />
这一段话可谓箴言。自由主义者，也应该记住。<br />
<br />
芦笛先生现在的作为，可以为名人所借鉴。<br />
<br />
5.<br />
芦笛先生在本质上是一位文学家。我这样说，并不是抹杀他作为学者的功底。<br />
<br />
一位文学家，往往能够看到凡人看不到的东西，或者看到了又不重视的东西。不仅如此，他还可以把看到的东西加以生动的描绘，给人留下深刻的印象。当然，如果看错了，也很容易误导大众。<br />
<br />
在有些方面，芦笛先生与柏杨先生有几分相似。芦笛的每一篇文章，都可以找出不少毛病。同样，他的几乎每一篇文章都能够给我们以启示。<br />
<br />
严谨的学者们，你们在指出芦笛的不足时，一定要反省一下为什么没有他的创见。<br />
<br />
6.<br />
在这个世界上，有些文人实际上是有特权的。在这些人中间有才华横溢的人，写作成就卓著的人，彻底的悲观主义者，等等。当然，其充分条件是他们的特权是自然形成的，且为社会所认可。说得直白一些，就是可遇而不可求。<br />
<br />
这些有特权的人，包括了陀思妥耶夫斯基，鲁迅等等。作为政治人物，有甘地和孙中山等等。现在的文人中间，芦笛可以算一位。<br />
<br />
试想，陀思妥耶夫斯基已经是彻底悲观了，他并没有试图构建什么体系。假如你去逐字逐句地批判他的作品，那就反而没有见识了。还有，热衷于打到鲁迅的人，其实是在和自己过不去。<br />
<br />
对于芦笛，我在读他的文章时，一般不在意其中错的地方，也不刻意去找闪光的句子。读着读着，就会有具备启发性的东西出现。<br />
<br />
当然，假如一个人有特权，那么在承认他的启发性的同时，也要降低其理论的可操作性。<br />
<br />
