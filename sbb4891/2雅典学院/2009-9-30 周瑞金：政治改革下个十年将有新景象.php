													<h2>政治改革将会出现新的景象</h2>
						
						
																											<div id="postmessage_264134" class="t_msgfont">我们可以预见，共和国下一个十年，我国政治体制改革和政治文明建设将会出现以下的新景象：<br />
<br />
依法治国成为中国共产党执政兴国的基本方略。作为执政党，中国共产党将进一步调整革命党的惯有思维，按照依法治国的要求，改革和完善共产党的领导方式和执政方式。党的领导将主要是通过制定大政方针，提出立法建议，推荐重要干部等途径，依法实施党对国家和社会的领导。有望着手起草制定“执政党与国家权力机关的关系法”一类法律，来完善执政党在宪法规定的范围内活动、依法执政的法律依据。<br />
<br />
以增强党的民主为中心，推动人民民主，不断完善社会民主制度。中国共产党将从改革体制机制入手，建立健全并能充分反映党员和党组织意愿的党内民主制度。如在部分县、市进行的党的代表大会常任制的试点，将会从下到上地进一步扩大到全国，以积极探索党的代表大会闭幕期间发挥党员代表作用的新途径和新形式。以保障党员民主权利为基础，进一步完善党的代表大会制度和党的委员会制度。党内选举将普遍实行差额选举，以保障党员自由选择的权利。建立一套民主程序，按照少数服从多数的原则，保证党委内部的议事和决策机制进一步民主化。中央委员会也会进一步贯彻民主选举制度。十七大前已实行一人一票不记名方式，进行民主推荐政治局委员与常委的候选人，一改以前仅在少数人范围内以征求意见协商方式产生候选人的办法。<br />
<br />
采取新举措新制度，不断加强对权力的制约和监督。未来必将加强对公务员，尤其是各级领导干部的监督制度，进一步完善党的纪律巡视制度，逐步建立领导干部财产申报制度。要完善重大事项和重要干部任免的决定程序，推行领导干部公推公选制度，建立党代会、人代会对领导干部问责和质询制度。重视发挥舆论监督的作用，制定新闻法保护媒体议政，对官员实施监督。<br />
<br />
进一步完善人民代表大会制度和多党合作制度，切实保证人民当家作主。人民代表大会制度和中国共产党领导的多党合作和政治协商制度是我国根本的政治制度。扩大公民有序参与，逐步实行城乡按相同人口比例选举人大代表，保障人民的知情权、参与权、表达权、监督权。进一步加强人大常委会制度建设，优化组成人员结构，增加法律与社会公共管理专业人才。增强决策透明度和公众参与度，今后凡制定与群众利益密切相关的法律法规和公共政策，人大必定公开听取人民群众的意见。同时，人大是我们国家权力机关，既有立法权，也有决策权、监督权，尤其要对政府监督问责。<br />
<br />
加快行政管理体制改革，建设服务型的法治政府。政府不要干预微观经济，不要站在招商引资第一线，不要与开发商打得火热，而要着力干好宏观调节经济、监管市场的事，把力气用在社会管理和公共服务上。尽可能减少行政审批，切实做到政企分开、政资分开、政事分开、政府与市场中介组织分开。加大机构整合力度，减少行政层次，降低行政成本。改变政府“重管理、轻服务；重经济、轻社会”的观念和体制机制，实现向服务型政府转变。推进政府的决策透明、财务透明、用人透明，合法行政，诚信高效，推进行政信息公开，善纳民众诉求，探索建立一个民评政府、民评官员的政府绩效评估体系，建设法治政府。坚决防止政府公共权力形成特殊的利益集团，防止让权力部门化、部门利益化、利益政策化的现象出现。<br />
<br />
发展基层群众自治制度，切实保障公民行使民主权利。我国长期实行由政府统包社会治理的“全能主义”政治模式，基层群众缺少民主权利。所以改变“全能主义”模式，构建三元格局的公民社会，即政府的公共权力、社会的自治权力、市场的配置资源权力，这样才能形成一个和谐社会。活跃民间组织，比如各种协会、学会、维权组织、慈善组织、志愿者组织，真正让他们担负起社会责任。他们将更为积极地发挥提供服务、反映诉求、规范行为的作用，增强社会自治功能。汶川大地震中志愿者、民间组织发挥了抗震救灾的巨大作用，标志着我们社会公民意识的觉醒，以及公民社会的发育成长。</div>

							
							
							
							
