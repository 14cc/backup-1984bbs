<br />
一次是1980年6月，中央政治局常委讨论肃清封建主义的影响和改革国家制度的问题。8月，中央政治局举行扩大会议专题讨论党和国家制度的改革，邓小平同志代表常委作了《党和国家领导制度的改革》的重要讲话，提出权力过分集中是传统政治体制的基本特征和总病根，党政不分、以党代政是传统政治体制的主要弊端，强调“领导制度、组织制度问题更带有根本性、全局性、稳定性和长期性”。后来，由于波兰 “团结工会”发动了工人大罢工，领导层担心国内局势震荡，便放缓了政治改革的步伐。<br />
<br />
时隔六年，1986年5月邓小平同志再次提出政治体制改革。6月28日在中央政治局常委会上，他发表讲话说，只搞经济体制改革，不搞政治体制改革，经济体制改革也搞不通，因为首先遇到人的障碍。从这个角度来讲，我们所有的改革最终能不能成功，还是决定于政治体制改革。党的十三大政治报告中第五部分集中论述了政治体制改革的内容，提出了党政分开、协商对话、重大事情让群众知道、推动机构改革、基层民主等内容。这场得到邓小平同志全力支持、反映了全党全国人民心愿的、成功在望的政治体制改革，由于1989年春夏之交一场政治风波，再一次失去良机。<br />
<br />
1997年春，中共十五大召开前夕，当时中央党校一批省部级官员曾向中央建议，在十五大重提政治体制改革任务。主持十五大文件起草工作的负责同志代表中央领导听取了他们意见后，曾代表中央领导说，在十五大到十六大之间中央打算召开一次中央全会专题研究政治体制改革问题。后来由于发生北约轰炸我驻南斯拉夫大使馆和“FLG”围堵中南海事件，专题研究政治体制改革的中央全会又被推延了。</div>

							
							
							
							
