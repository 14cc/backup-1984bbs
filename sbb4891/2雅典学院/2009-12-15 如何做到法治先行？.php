																											<div id="postmessage_376194" class="t_msgfont">近期内司法很难作为社会正义的最后防线<br />
<br />
11月16日的南都社论以“还原司法作为‘最后防线’的职能定位”为题，再一次重申了司法独立的重要性。笔者赞同司法独立的追求，也同意“司法是社会正义的最后一道防线”应作为追求的目标，但是，如果考虑到中国的体制现实，却又对此很难乐观。<br />
<br />
说到最后防线，就不能不想到中国的信访制度，目前，蔚为大观的上访民众已经成为了一道风景，他们将权利救济的寻求方向主要指向了行政部门而非司法部门，有见于此，一些学者认为，信访是正常司法救济程序的补充程序，其目的通过行政方式来解决纠纷和实现公民的权利救济，不过是与行政诉讼、行政复议等行政救济手段并列的普通手段，而国家的司法救济才应该是公民权利救济最为主要的形式，因此，把信访看成了优于其它行政救济甚至国家司法救济的一种特殊权利，不啻是一种对司法权威的侵害, 乃是一种人治的遗留，它损害了法治的健全, 从体制上动摇了现代国家治理的基础。以此出发，这些学者建议应反思现行信访制度，并不排除在适当条件下废除这一制度。<br />
<br />
可是，为什么信访会被看作是一种优先和特殊的救济权利呢？由于救济旨在通过某种积极方式的运用，而使受损权益得到恢复或者补救，因此，救济的运用就不能不体现为某种权力关系，只有以更高的权力为后盾，才能对较低的权力所造成的损害加以恢复和补救。普通百姓或许并不能如此学理而分析，但他们的理智却足以辨认出究竟谁拥有更高的权力，谁更可能给予他们以救济和帮助。在行政为主导的当代中国，不仅在理论上司法体系从属于各级人民代表大会，在实际中，各级党政机构也拥有高出于司法体系的权力地位，因此，对于访民来说，在目前的体制下把信访看作优先手段就是一种理性的选择，和他们谈什么司法作为“最后防线”几乎没有任何说服力，学者们需要回答的是，如果废除了现行信访制度，那么，该用怎样的制度来替代，以更好地满足他们的权利救济要求？<br />
<br />
作为一个理想目标，几乎所有人都会觉得司法独立值得追求，但是，最后防线所在，也就是最高权力所属，权利救济离不开权力的安排，司法如果要成为社会正义的最后一道防线，则它至少应成为最高权力的一部分。这一点可以从司法独立比较完备的美国得到印证。尽管美国最高法院大法官由行政分支的总统提名，并需经过立法分支的参议院的同意，但司法分支并没有屈居于其他两分支之下，因为，无论是总统签署的法令，还是参众两院通过的法律，都可能经过层层上诉而最终提交最高法院裁决。更为戏剧性的例子是2000年的美国大选，由于在佛罗里达州出现计票纠纷，最后，在经过36天的难产后，由最高法院票决决定了总统归属。因此，在我的理解，司法分支只有通过分享最高权力――事实上更可能是以一种被动的方式掌握了最高权力，才有可能成为&quot;最后防线&quot;, 而这显然是与当前中国的制度现实相违背，前文已经指出，从理论上讲，全国人民代表大会才是国家最高权力机关，而实际上党中央国务院才是最高权力的行使者，司法欲作为“最后防线”，势必涉及到最高权力的安排,需要的是一场彻底的宪政变革，路漫漫其修远兮。<br />
<br />
可能正是因为这样的阻力，笔者注意到，学者们不约而同地提出，在废除现行信访制度后，应将类似职能交予人民代表大会也就是各级国家权力机关，如胡星斗教授提出，要在人大设立冤案申诉局,于建嵘博士的远期目标也是“把信访集中到各级人民代表大会，通过人民代表来监督一府两院的工作,并系统地建立民众的利益表达组织。”，这表明，这些学者准确地把握到了权利救济背后的权力关系，也把握到了信访优先所折射出来的现行权力安排。进而，将信访交予各级人民代表大会，至少在理论上与现行体制更少冲突，从而更具有建设性和可行性。<br />
<br />
当然，这并不意味对司法作为“最后救济”的远景的放弃，司法独立始终是一个可欲的目标，然而，笔者也不得不指出，格于制度现实，这可能只是一个比较远期的目标，难解近渴。</div>

							
							
							
							
