																											<div id="postmessage_375694" class="t_msgfont">说老实话，“法治先行”这句话我本来就不怎么赞同，不是不赞同法治本身，而是不知道它怎么个先行法。但很多学者都这么主张，我也姑且同意它应当先行。但怎么个先行法的问题依然存在。想了半天，好像只有三途：1，由现有的立法和执法机构去立法执法。可这样，很多学者和百姓信不过，说他们本身是统治者和既得利益者，由他们立法执法肯定偏向他们自己。瞧见了吧，行不通。那么，2，选举，通过竞选上去的人立法执法。就算现在当局允许选举，那么什么人最有可能上台呢？一位网友说过：查韦斯。我也觉得很有可能。因为现在的气氛就是民粹嘛，查韦斯不上台谁上台？可是，查韦斯上台的后果是什么？国有化、（企图）职务终身化！这样的结果一定是我们再熟悉不过的了――民不聊生！大家同意吗？好像不同意。那好，又行不通。那就只剩――3，精英立法。可是，谁是精英，怎么成为精英，这些精英又如何进入立法和执法机构？主张“法治先行”的人似乎没有回答这个问题，刚才老莫也没有回答。可是，这个问题不回答，“法治先行”不就成了一句空话？况且，这个精英立法本身也会遭到反智者（可能主要是穷人）的反对，说，凭什么精英就可以立法而我们就不行？<br />
<br />
这个问题留给网友共同回答。</div>

							
							
							
							
