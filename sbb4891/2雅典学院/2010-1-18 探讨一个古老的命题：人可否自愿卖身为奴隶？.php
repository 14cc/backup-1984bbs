<br />
在一个强调积极自由的社会里，他应该具备这个自由<br />
在一个以实现消极自由为目标的社会里，成为奴隶这个事情是违反消极自由的，所以会被禁止<br />
<br />
但是现实中，要是允许个人成为奴隶而有很多人出于自身穷困等原因愿意卖身为奴的话<br />
一个以提高积极自由为目的的社会应该采取手段来增加个人的赚钱能力，避免这样的事情发生<br />
所以现实中不管什么样的社会，至少都会避免这样的事情</div>

							
							
							
							
