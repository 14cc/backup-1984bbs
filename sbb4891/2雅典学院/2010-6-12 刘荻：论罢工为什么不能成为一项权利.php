													<h2>刘荻：论罢工为什么不能成为一项权利</h2>
						
						
																											<div id="postmessage_677223" class="t_msgfont">近期，富士康十三连跳、广州本田罢工等事件接连发生，民间对此多有议论。最近读到许志永老师和其他其他一些论者主张“罢工应该成为一项权利”的文章，笔者对此有不同意见，故撰此文与之商榷。<br />
<br />
1.许志永等论者虽然主张罢工应该成为一项法定权利，但都不认为个别工人有权罢工，事实上他们都主张只有工会才有权罢工，个别工人罢工应该被视为非法。按此观点，罢工并不是工人的权利，而是工会的特权。<br />
<br />
2.如果罢工不是工人的权利，而是工会的特权，那么论证工会是否应该有权罢工，就不能用工人在资方面前弱势来论证，而应该用工会在资方面前是否弱势来论证。工会一般来说并不比资方弱势，甚至可能比资方更强势，因此给予工会特权来平衡所谓的资方的强势的观点是站不住脚的。<br />
<br />
3.工人不仅在资方面前可能是弱势的，在工会面前也可能是弱势的，给予工会特权可能只是加强了这种权力不平衡而已。<br />
<br />
4.有人认为工人是自愿参加工会的，因此工会必然代表工人的利益，工会和工人之间不会存在利益冲突。持这种观点的人显然忘记了，工人肯定也是自愿加入富士康等企业的，而他们并不认为富士康必然代表工人的利益，或者富士康和工人之间不存在利益冲突。<br />
<br />
5.有人认为工会是民主决策的，因此一定代表工人利益，但民主决策也能说明工会的利益和民主决策中的少数派工人的利益并不完全一致。用另一个例子来说明：人们并不认为民主国家的国家利益与每个公民的个人利益完全一致或者没有冲突。<br />
<br />
6.有些工人不愿意参加工会，给予工会罢工的特权很可能损害这些工人的利益。尤其是工会在罢工时通常会组织纠察线，阻止拒绝罢工者工作，这就侵犯了这些工人的利益。<br />
<br />
7.根据以上几点我们可以得出结论说，认为工会必然代表工人的利益，这种观点是没有根据的，因此用工人在资方面前的弱势来论证应该给予工会罢工的特权，也是没有道理的。<br />
<br />
8.如果认为只有工会有权罢工而工人无权罢工，那中国政府禁止罢工也是完全合理的，因为只要中国官方工会不出面组织罢工，那一切罢工行为自然都是非法的，那么在中国谈论罢工权就是毫无意义的。<br />
<br />
9.因此，综上所述，认为工会有权罢工而工人无权罢工，这种观点是站不住脚的。<br />
<br />
10.那么认为每个工人都应该拥有罢工权，这种观点是否站得住脚呢？这种观点仍然站不住脚，因为罢工是一种违约行为，而法律不能把违约规定为一种法定权利，就像法律不能把违法规定为一种法定权利一样。虽然有些论者提出在某些情况下，罢工可能不构成违约，例如在合同到期或未做明文规定的情况下，但不可否认的是，大量罢工都构成了违约。<br />
<br />
如果法律把一种普遍构成违约的行为规定为法定权利，那就等于取消了契约和权利这两个概念。如果契约的一方有权随时违约而不承担违约责任，那就根本不会有契约存在了；如果每个工人都有权罢工，那就根本不会有劳动合同存在了。另一方面，如果某些罢工既不构成违约，也不构成违法，那么根据法无禁止即自由的原则，将其规定为法定权利也是没有必要的。<br />
<br />
11.我们可以把罢工与非暴力抵抗做个比较。非暴力抵抗通常是要违法的，而且很多人都认为为了一个更高的正义目标而违法，在道德上是可以接受的。但是无论目标有多么正义，违法本身也不能成为一项法定权利。</div>

							
							
							
							
