&quot;为什么偏偏在这个共产党一党专制、没有民主选举产生的议会、没有司法独立、没有新闻自由的国家，经济会繁荣不衰？&quot;这是《时代周报》一篇署名文章在德国总理默克尔访问中国之际提出的问题。<br />
