<br />
今天有人在谈论'北京共识'，它与高举市场自由、贸易自由、尤其强调社会自由的'华盛顿共识'不同，是另一种选择。美国作者哈尔珀推出的新书《北京共识》试图证明'中国的威权模式将如何掌控21世纪'，哈尔珀的论点是，与全球化缩小了世界一样，人民共和国在悄悄通过限制西方价值观的传播使西方收缩。有一点不容忽视：经过世界金融危机后，中国变得更为强大。<br />
<br />
