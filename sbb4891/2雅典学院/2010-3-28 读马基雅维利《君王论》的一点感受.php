																											<div id="postmessage_535970" class="t_msgfont">读马基雅维利《君王论》的一点感受<br />
<br />
作者:再世关羽（对了，原创。好久没写点什么了）<br />
<br />
马基雅维利的《君王论》一直是一本饱受正义的书。很多人都将其<br />
与《韩非子》相提并论，对其进行口诛笔伐。而我在阅读这本书的<br />
过程中，却着实从中感受到了很多值得我们民主人士学习，也是我<br />
们民主人士所缺乏的理念。<br />
<br />
以下就是我的几点心得。<br />
<br />
1.关于民粹的问题<br />
马基雅维利认为，统治者需要走向民众，在民众中生活，与民众和<br />
谐共处。我们应该认识到，无论是专制政体还是民主政体，政权的<br />
基础都是被精英说鄙视的基层人民。像刘晓波，十岗等“民主精英”<br />
那样无视民意，蔑视民粹的行为，无疑是一种将民主人士与群众对<br />
抗，把民主人士放到火中烤的错误行为。我们当然不该受到民粹的<br />
左右，走向彻底的民粹主义泥淖。但是，马基雅维利已经告诉我们<br />
如何应对错误而广泛的民意的方法――人民往往是恶劣的，而且根<br />
本做不到守信不渝。因此统治者无须完全听从他们的意见；但是！<br />
统治者必须要掩藏这种思想，在表面上还是要做到听从，关心名义<br />
（哪怕是如同演戏一般）。尽管某些“知识精英”对这种“违反道德”的<br />
行为大加鄙夷，但我们要认识到，邪恶的希特勒和毛泽东利用这种<br />
思想取得了广大百姓的拥护并最终夺得了政权，而理想主义者罗伯<br />
斯庇尔和阿连德，他们的尸首却在绞刑架上摇曳。<br />
<br />
2.目标与手段问题。<br />
中国有句古话：正人邪法，邪法亦正；邪人正法，正法亦邪。马基<br />
雅维利有着类似的思想。他认为：忠实的奴仆总是当奴仆，诚实的<br />
人永远受穷。除非既大胆又不忠实，否则永远也摆脱不了奴役；除<br />
非既贪婪又奸诈，不然一辈子也逃不出贫困。上帝和大自然把所有<br />
人生的幸福都撒到人间，人们把它抓到手的方法与其说是勤勉不如<br />
说是强夺，是恶行而不是善举。”这是何等的真知灼见！是的，政治<br />
的真相就是这样，政治本身没有什么对错之分，只有利益的纠葛，<br />
权力的斗争。在这样的环境中，一味对手段的善恶耿耿于怀无疑是<br />
签署了自己的死刑判决书。要知道，政治是肮脏的，只有目的才有<br />
善恶之分。任何心存幻想，以为政治斗争可以是干净的，不流血的<br />
人无疑会成为失败者。只有“拥有狮子般威严，狐狸般狡诈”的政治<br />
家，才能在政治斗争中笑到最后；而那些过分执着于世俗道德，不<br />
知变通的“刘晓波，余杰”型人物，则“常常会因为过分在意世俗美德<br />
，从而丧失对局面的有效控制，从而导致组织的毁灭。”无疑，现在<br />
在监狱中的刘晓波，就是这些人的真实写照。我们必须认识到，我<br />
们民主人士身负全中国未来的希望，所以我们的目光不能被虚妄的<br />
道德所束缚，要为善，更要为达成善的目标而“为恶”。至于刘晓波<br />
们，不客气的说，他们连承担骂名的勇气都没有，如何能够领导我<br />
们完成反共救国的宏伟事业？<br />
<br />
3.关于社会建设的问题。<br />
很多人以为马基雅维利是一个不择手段的君主制维护者，一个“意大<br />
利的姚广孝”型狗头军师。这种说法是完全错误的。马基雅维利在《<br />
君王论》中不止一次的阐述过君权来自人民的理念。他对于政权组<br />
织的建设，更是提出了“如果组织成员有权表达不同意见，并能在组<br />
织内部解决彼此冲突，那么组织就会更稳定”的民主议会理念雏形，<br />
更是明确的表示了“如果有很多人负责并许多人了与维护它（国家）<br />
，那么，这个组织（国家）就会更长久。”这种对民主社会的向往。<br />
同时，马基雅维利“新的统治者统治被征服的地区，应该至少在表面<br />
上保留一些旧习”这一观点，又否认了俄国十月革命那种“横扫一切<br />
旧事物”的极端激进革命思想。<br />
<br />
4.关于人性的问题。<br />
马基雅维利无疑是一位深刻认识到人性本恶这一真理的先哲。他在<br />
《君王论》的序言中将他对人性的认识以赤裸裸的语言表露出来&quot;关<br />
于人类，一般可以这样说，他们是忘恩负义、容易变心的，是伪装<br />
者、冒牌货，是逃避危难、追逐利益的。因此，谈论人的道德尊严<br />
和谈论人生高贵的超自然目的就毫无意义，试图哟哦那个自然规律<br />
或神圣法则的支配来约束人也是枉费心机。&quot;这就告诉我们民主人士<br />
一个民主精英至死也不愿承认的问题。那就是指望中国人自己觉醒<br />
，接纳民主宪政思想是不切实际的。民主者应该“以利反共”，利用<br />
百姓的自私自利之心来开展民主思想和反共理念的宣传，而不是用<br />
与百姓眼前利益毫无瓜葛，白白惹厌的“08宪章”去丢人现眼。别忘<br />
了宪章的主要签署人――维权上访群体，如果他们的眼前利益没有<br />
被TG侵犯，他们会对08宪章有这么大的兴趣吗？<br />
<br />
5.关于武装革命的问题。<br />
“所有的武装的先知都取得了胜利，而没有武装的先知只能被别人消<br />
灭。”马基雅维利告诉我们民主人士，在一个专制独裁的社会里面，<br />
靠嘴皮子获得政权是不可能的妄想。只有拥有了自己的武装，我们<br />
才有获胜的可能。政治斗争如同商战，手中没有筹码，想要空手套<br />
白狼的人下场必定十分凄惨。而对于专制暴君来说，他依赖，也是<br />
最害怕对手拥有的筹码，就是武力。而拥有武力之后，我们是不是<br />
应该选择像当年那个不明智的孙中山一般妥协呢？大错特错。马基<br />
雅维利早就对这种妥协思想给予了极度的鄙视。他说“如果有谁相信<br />
一个大人物会因为得到新的恩惠而忘记前仇，那真是自欺欺人的想<br />
法。”专制权力是会使人上瘾的。我们将对手赶下了王座，就要做好<br />
他们反扑的思想准备。因为群体性权力瘾的戒除方法，只有群体性<br />
死亡。无论如何温和的妥协都是在损害现在的共产党既得利益集团<br />
的利益，而“我们要损害一个人，这种损害要彻底并且不遗后患，这<br />
样我们就不会再担心给他留有任何可能进行报复的机会。”<br />
<br />
最后，任何思想都是随着时代的变化而不断进步的，死守教材不知<br />
