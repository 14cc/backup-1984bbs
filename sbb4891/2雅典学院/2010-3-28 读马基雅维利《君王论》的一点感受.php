<br />
克劳塞维茨留给我们的价值是战争思想，也可以叫做战争哲学。这是克劳塞维茨思想的凝练。最近我读索科拉夫斯基的现象学导论，他在导言中说，科学把前辈凝练出几个基本结论，后人由此前行；人文科学正好相反，每一个人对一位先哲的解读都有不同，这就是人文科学裹足不前的原因。我们为什么不能把先哲的思想凝炼、去粗取精，形成后人前进的平台呢？<br />
<br />
比如我们对伽利略、牛顿、爱因斯坦、海森堡、波尔等都能用几句话概括他们的思想，甚至康德的星云说、笛卡尔坐标和罗素的数理逻辑都能几句话概括清楚，可是我们却没能用几句话概括柏拉图、亚里士多德、奥古斯丁、托马斯.阿奎那、维特根斯坦，黑格尔、海德格尔和胡塞尔，以致我们也不能概括出康德和罗素的哲学思想。对这些人几乎一个人有一个解释。<br />
<br />
