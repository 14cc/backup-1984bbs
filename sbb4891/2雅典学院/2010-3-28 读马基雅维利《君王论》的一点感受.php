																											<div id="postmessage_536852" class="t_msgfont">我不知道是我们不准进口游戏机，还是人家不打算出口。汗。我觉得是后者。因为游戏机这东西是赔钱的，人家是靠卖软件赚钱的，中国人又不可能买正版软件，出口到中国岂不是亏了？当然，我也很多年没关注这个市场了，最近的情况完全不知道。<br />
儒家思想，其实别的还好，当然你肯定说都不好...问题是基督教什么的也一样不好，传统思想都有类似之处。<br />
儒家有一个独特的坏处就是：礼，这不是西方人会特别强调的东西，但也不是枝节；如果是枝节，谁会刻意强调它几千年？礼这个玩意儿就是为了维护等级制度的，现在尤其方便维护砖家叫兽。<br />
&lt;&lt;&lt;&lt;&lt;&lt;<br />
每个知识分子都有一个很特殊的责任。他拥有学习的特权和机会。作为回报，他对于同胞（或对于社会）有责任尽可能简单、清楚、谦虚地描述他的研究结果。知识分子所做的最糟的事情――主要罪过――是试图对同胞自命为伟大的预言家，给他们留下令人迷惑的哲学的印象。任何不能简单、清楚地讲话的人都应住口，继续下些功夫，直至能这样做为止。－－卡尔.波普尔<br />
&gt;&gt;&gt;&gt;&gt;&gt;<br />
而儒家关于礼的说法，等于是说：砖家叫兽可以不谦逊，别人必须谦逊地对待他们，这就完全搞反了。<br />
此外，孝道也不是什么好东西。<br />
但是，即便如此，我们也不便对儒家进行针锋相对的反对，这么干的结果只能是得罪民族主义者，完全不策略。对付儒家的办法，仍然可以采取一种对付所有传统都有效的办法：不承认它是传统。<br />
也就是说，在环境变化了的情况下，任何传统都不可能直接搬到现代来，任何人都多少要进行变通；只要他进行了变通，我们就可以否认他的正统性，不承认他。比方说”孝“。谁敢说”二十四孝“那样的糟粕可以搬过来用？（说实在的，我很怀疑它在任何时代被真用过）只要你提倡孝道，我就拿”二十四孝”的不可能性来嘲笑你，有种你就来真的，没种就闭嘴。<br />
而且，我仍然认为，现在儒家的”兴旺“也好，老毛反儒的“兴旺”也好，都是权力造就的，毫无理论基础，只要权力易手，也就不在话下了。从老毛的反儒到现在的“尊儒”，恰恰说明权力决定一切，儒家并无力量，不值得过于认真对待。</div>

							
							
							
							
