													<h2>陈维健：中国人是猪你同意吗？</h2>
						
						
																											<div id="postmessage_814582" class="t_msgfont">近日，在南太平洋的新西兰，全国最大的城市奥克兰繁华的皇后大街，一位留学生模样的华人男子，胸前挂着一块英文牌子上面写着：<br />
&nbsp; &nbsp;&nbsp;&nbsp;“中国人是猪你同意吗？”<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;然后例出几条理由：<br />
&nbsp; &nbsp; “百分之九十的中国女性支持全球性的性服务”<br />
&nbsp; &nbsp; “百分之九十的中国人不认同祖国，不认同祖先”<br />
&nbsp; &nbsp; “中国人很贱，这是猪的哲学文化”。<br />
&nbsp; &nbsp; 在他的身边放着另一块牌子，“如果你认同我的观点，请给我一点硬币支持我站在这里，如果不认同，感谢你的阅读，但不要与我争论。”<br />
&nbsp; &nbsp; <br />
<br />
&nbsp; &nbsp; 这样出位的表达方式，引来路人的围观和议论，也引起华人观者的愤怒，在二三天的时间里，无数的华人对他进行语言围攻甚至动粗，但都在街人的劝阻下没有造成事件，在此期间也有许多华人报警，但均无警方到达。直到第三天，一位戴眼镜的留学生，后来被网络称之为“眼镜男”对其动粗，并抢夺其宣传牌，被附近的一位保安目睹，并因劝阻末果而报警，警方即刻赶到，围堵抢夺者将其铐住，同时也将持牌者带往警局问话。事发后整个华人社会为之“沸腾”，纷纷谴责持牌者辱骂中国人，赞扬眼镜男的英雄行为，此事几天来在华文媒体持续发酵，辱骂谴责之声汹涌澎湃，老年华人也气急挥拳，一位奶奶级的女士声称“如果我在场的话，虽然年龄大了，也要与他拼个你死我活”。有的说如果我在场，我要用漆喷到他的脸上，看他还敢不敢侮辱中国人。这些暴力性的语言不一而足。眼镜男以武夫眉笔的网名以英雄的面目出现，号召华人起来共同应对这一事件，并对其可能将面临的指控给予帮助。这一事件成了华人表达爱国情怀的公共平台，大有不在此事件上出来说上三言二语，表达愤怒的话，就对不起列祖列宗了。<br />
&nbsp; &nbsp; <br />
<br />
&nbsp; &nbsp; 对于皇后街上所发生的这起辱华事件，只要平心静气地观察和看待，完全没有必要掀起如此轩然大波。我们从持牌青年木纳的脸部表情和持牌内容来看，不难发觉此人在精神上可能会有某些问题，他骂中国人是猪的立论，假设的前提都是错误的，且语无伦次，逻辑颠倒，百分之九十的统计也完全没有依据。如果真的要骂中国人，中国人的国民性中有太多的丑陋之处，可以来作立论，柏杨就有“丑陋的中国人”一书。对于这样一个人，我们首先不应该动气，应该想想，这个人是出自何种原因持牌骂中国人。是不是他碰到了什么令他生气的事情，或者受到同胞的欺侮，遭女朋友抛弃，而心灵受到伤害，精神受到刺激，心理上出现问题。我们常常讲，“天下没有无缘无故的爱，也没有无缘无故的恨”，他这样做总是有他的原因在里面，同是华人应该对他有一份关爱和帮助。如果说，年轻的华人逞一时之气而动粗，年轻气盛还情有可原的话，那么作为老人就千不该、万不该以这样的态度来对待此事了，特别是老妇，妇人总该有妇人之心，一个孩子以这样极端的方式孤零零地站在街上，应该心生怜悯才是，古德云“幼吾幼，及人之幼”，如以这样一种情感待人，当柱一根拐棍，去为他送汤倒水，抚一抚，摸一摸，暖语相安，好言相劝，哪里还会恶言相加，甚至报以老拳。但是这样一种情感已经长期地远离了我们的民族，现在六十岁以上的老人，其思想成熟期，正是喊打喊杀的“文革”年代，在这样一种政治至上暴力文化熏陶中，沉淀下来的思想和行为方式，使他们在任何一个场合，任何一件小事上，都会抛掉人的基本情感，比如说老人对孩子的情感，把它上升到国家和民族荣辱的高度。这是这一代人的可悲之处。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 在这件事件中很多本地的洋人目睹了其过程，从西方文化来看，举一块牌骂骂自己人并不是一件什么大的事，这种事在他们的社会里多得去了。当年奥市举行同性恋大游行，市长因没有同意拨款支持，遭游行者漫画为猪，由一性感女郎鞭抽屁股，大家看了一笑了之，市长也没有动气。新西兰人还自誉为KIWI，这是一种纽西兰特有的鸟，其因又聋又瞎还不会飞，处在生存淘汰阶段。他们很难明白这么多华人，为什么对自己的同胞骂一声“猪”要动那么大的气。其实这件事的过程也是表达一个民族文明的过程。前不久台湾作家龙应台在“北大”演讲时说，看一个国家是否文明有很具体的东西，其中就是如何对待“精神病患”。这个年轻人的精神状况是可见的，当我们见到这个年轻人，看到他的举措，不是报警而是联系社会关爱部门才是，这样的部门在新西兰非常的多，也有华语人员。即使认为他是一个精神健全人的有意恶骂，也应该以文明的方式进行规劝，以长者之心，手足之情，同胞之爱，去感动他。也让本地人看看，中国人是如何以一种包容的爱心对待一个辱骂自己人的同胞。但是很遗憾他们看到的是粗暴。那些因爱国激情而动粗的人应该想想，自己的行为是在为民族争光，还是在败坏中国人的文明。<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 我们爱国，是爱我们这个国家、民族的文明精神，我们这个民族有着太多的传统美德，宽宏、包容、仁爱、慈悲，但是这些精神到哪里去了呢？是什么东西使我们这个民族变得如此狂躁不安，经不起一点小小的刺激，动辄动气动怒，粗暴残忍呢？被称为人生三大喜事之一的“他乡遇故人”，更是中国文化中独特乡土情怀，为百代文人墨客传颂，常常一唱三叹，情动天地，绵绵不绝于中华民族的历史时空。但是这样一种美好的风尚情感时至今日，不但无迹可寻，反演绎出一部又一部的“老乡见老乡，背后放一枪 ”的悲剧。在新西兰这个遥远的他乡，一个同胞，一个年轻人精神失意时的一句辱骂，竟然使同胞如丧考妣，兴师动众，非置其于死地而后快不可。当我们在痛斥阿富汗塔里班，用石头砸死通奸的男女的野蛮时，我们又是以怎样一种性情行为展现中国人的文明？<br />
