													<h2>亡国：日本人民幸福的起点</h2>
						
						
																											<div id="postmessage_758227" class="t_msgfont"><a href="http://laoguoxuetu.blog.sohu.com/156997496.html#comment" target="_blank">http://laoguoxuetu.blog.sohu.com/156997496.html#comment</a><br />
<br />
1<br />
<br />
当麦克阿瑟将军的座机降落在东京机场，当美国五星上将麦克阿瑟将军走出飞机踏上日本的土地，尽管他没有穿军装，也没有携带任何武器，更没有举行什么入城式阅兵式，但这一刻，1945年8月30日下午2时5分，对于7000万日本人来说，依然是前所未有的恐怖和残酷，亡国，亡国，亡国，终于变成现实了。<br />
<br />
 <br />
<br />
2<br />
<br />
日本人从来没有做过亡国奴，甚至日本本土从来都没有被异族占领过。1274年和1281年，强大的不可一世的蒙古大军曾经两次进犯日本，但都被神奇的海风挡住了。<br />
<br />
但是，自从1853年美国海军准将佩里率领舰队造访日本以来，自从西方人向日本人展示了“西方那一套”的强大以来，日本人，尤其是精英之士，意识到亡国的危险就在身边了。从那以后，他们一直恐惧着防范着“亡我之心不死”的西方人的入侵，竭尽全力避免亡国噩梦变成现实。为此，他们发起了明治维新，搞起了开放改革；他们奋发图强，扩张国力；他们还把防御的心理边界和现实边界一再外延，不惜发动侵略战争，不惜先下手为强挑起世界大战。然而，在付出了七十多年的努力之后，在付出了惨痛的血腥的代价之后，在把日本的城市变成了焦土瓦砾之后，还是没有挡住亡国这一天的到来。<br />
<br />
日本终于亡国了。<br />
<br />
 <br />
<br />
3<br />
<br />
没有料到的是，绝大多数日本人完完全全没有料到的是，亡国给日本带来的不是千百万人头落地，不是受几茬苦遭几茬罪，不是被欺凌被压迫被宰割，而是，而是，而是，前所未有的幸福。<br />
<br />
亡国给日本人民带来了前所未有的幸福。<br />
<br />
 <br />
<br />
4<br />
<br />
我们来看看，以麦克阿瑟为首的美国占领军当局在占领日本之后都做了什么，在征服的土地上，在无条件投降的日本国土上，胜利的征服者们究竟做了些什么。<br />
<br />
（1）&nbsp; &nbsp;&nbsp;&nbsp;还是在飞往日本的飞机上，也就是1945年8月30日，麦克阿瑟就向他的幕僚们谈起了他统治日本的设想，归纳为两个要点：<br />
<br />
第一点：铲除日本文化和行为中对外侵略的因素；<br />
<br />
第二点：促进民主在日本成长。<br />
<br />
（2）&nbsp; &nbsp; 麦克阿瑟抵达日本后，下令释放了被日本政府长期关押的包括许多共产党人在内的政治犯，一贯反共的美国人解放了身陷囹圄的共产主义者。1945年10月，这些出狱的共产主义者合法地组建了日本共产党。<br />
<br />
（3）&nbsp; &nbsp; 1945年8月25日，美国占领军允许日本妇女建立自己的组织；9月，公布了给予日本妇女选举地位的法案；12月17日，日本妇女历史上第一次获得了选举权。<br />
<br />
（4）&nbsp; &nbsp; 1945年10月11日，麦克阿瑟发布公告，解除了对报纸的禁令，日本实现了新闻自由和言论自由。麦克阿瑟在公告中还特别指出：要实现宪政体制自由化，要解放妇女，鼓励成立劳工组织，学校实行更自由的教育，实行经济制度民主化。<br />
<br />
（5）&nbsp; &nbsp;&nbsp;&nbsp;1945年12月22日，颁布了《工会法》，工人阶级真正地拥有了自己的组织。1946年9月27日，颁布《劳动关系调整法》，其中包括禁止企业开除或歧视罢工工人的条款。1947年9月1日，颁布了《劳动基准法》，规定了最低工资标准和最长劳动时间。世界上头号资本主义国家派出的占领者们却真正地从法律上保障劳动者的利益，保障工人阶级的利益，真是不可思议呀。<br />
<br />
（6）&nbsp; &nbsp;&nbsp;&nbsp;1945年冬1946年，美国紧急运送粮食、奶粉和肉类给日本。<br />
<br />
（7）&nbsp; &nbsp;&nbsp;&nbsp;1946年2月3日，麦克阿瑟指示盟军总部起草日本宪法样本。美国政府早在1945年19月给麦克阿瑟下达的制定宪法的准则是：日本政府必须绝对由全体选民授权并对全体选民负责。5月3日盟军提交了宪法草案。10月7日，日本国会通过了宪法。11月3日，天皇颁布新宪法。这是一个由占领者由外国人由敌人由西方人按照西方的价值观按照西方那一套强加给习惯于东方这一套的被占领者的宪法，但却是给被征服民族被占领国家的人民带来了民主自由法治的宪法，带来了和平繁荣昌盛的宪法，带来了幸福的宪法。<br />
<br />
“麦克阿瑟”宪法强调了日本人的基本公民权利，把这些权利视为“天赋而不可剥夺的权利”加以保障。这些权利包括：选举权；集会与出版自由；没有律师的即时介入，任何人都不得被逮捕定罪；保障人民居住安全，禁止无端的搜查与剥夺等。<br />
<br />
当时，除了日本共产党，所有的日本政党都赞成新宪法。<br />
<br />
（8）&nbsp; &nbsp; 1946年10月21日，国会通过《土地改革法案》。日本政府购买了所有外在地主的土地和在乡地主的多余土地，再把土地转卖给没有土地的农民。对没有钱买地的农民，政府给予抵押贷款。一夜之间，不流一滴血，不杀一个人，所有的无地农民都获得了真正属于自己的土地。<br />
<br />
（9）&nbsp; &nbsp;&nbsp;&nbsp;1947年3月31日，颁布了《教育基本法》。宣布教育的首要目标是“尊重个人尊严，努力培养人们热爱真理与和平。”教育的另一个目标是“建立一个民主、文明的国家，为世界和平与人类福祉作出贡献。”日本的学校不再被政府所控制，而是由公众选举的教育委员会管理。选择教师、课本和设定课程完全由民间自主决定。<br />
<br />
（10）&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;美国占领军当局是1952年归政于日本政府的。这亡国的7年，彻底地改变了日本的发展途径，把国家主权从专制者那里转移到了日本人民手里，引发了日本的根本性的进步，把日本引上了康庄大道。十几年后，日本就发展成为世界第二号经济强国，国家繁荣，人民富裕，社会稳定。<br />
<br />
顺便说明，美国占领军当局并没有花费日本纳税人的钱，他们的花销是由美国纳税人买单的。<br />
<br />
 <br />
<br />
5<br />
<br />
为什么亡国会带来人民的福祉？<br />
<br />
因为，所亡之国是专制之国，是少数人垄断权力剥夺多数人权利与利益之国。由于征服者来自文明的民主国家，他们带给占领地人民的是保障所有人权利的价值观和秩序，所以，必然推动社会进步。<br />
<br />
当野蛮征服文明的时候，带来的必然是灾难。当野蛮征服野蛮的时候，延续或者加深的依旧是灾难。而当文明征服野蛮的时候，带来的则是进步。<br />
<br />
当文明的民主战胜野蛮的专制时，带来的是民主的进步。日本、德国和意大利战后的历史，东欧国家战后的历史，从正反两方面充分证明了这一点。</div>

							
							
							
							
