<br />
在存中剑看来，吕加平是一颗棋子。这里并没有对吕加平先生的任何贬意，对于这位敢于冒生命危险揭露汉奸军委主席的勇士，存中剑是非常钦佩的。只是胡锦涛支持他去公开揭露江泽民的“二奸二假”，此事非同小可，若没有胡本人的点头，难以想象吕加平会主动公开这一事实并披露其中细节。当初吕加平不提胡锦涛在背后的支持，那是斗争形势的需要；今天他公开披露这一事实，这同样是斗争形势的需要，说穿了就是胡锦涛的需要。<br />
<br />
谁都知道江胡之间的斗争，可是多年以来一直是暗战，双方谁也没有撕破脸。今天吕加平主动站出来披露胡锦涛对他的支持，实际上就是胡锦涛对江泽民的公开宣战。在隐忍多年，布局多年，暗战多年之后，胡锦涛终于挥刀上阵了。借着吕加平的嘴，胡锦涛显露出了难得一见的强悍，公开对江泽民开骂：“老子就是吕加平的后台，就是用他来整治你，你现在还能拿老子怎么样？狗汉奸，你的死期到了！”<br />
