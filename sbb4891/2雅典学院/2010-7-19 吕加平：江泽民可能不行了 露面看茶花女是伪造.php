																											<div id="postmessage_747622" class="t_msgfont">吕加平：“二奸二假”上书 胡锦涛答复很微妙 <br />
――“二奸二假”是谣言还是事实？胡锦涛无以辩驳/吕加平 <br />
<br />
<br />
<br />
上面对“二奸二假”一文“不答复”的答复与该文究竟是谣言还是事实？<br />
<br />
&nbsp; &nbsp; 吕 加 平<br />
&nbsp; &nbsp; (2010年7月7日 抗日战争爆发七十三周年)<br />
&nbsp; &nbsp; “二奸二假”一文，即《关于江的“二奸二假”和政治诈骗问题与要求调查的呼吁》，我是花了整整十天时间在2009年11月30日写好的，写好后的第二天，即12月1日上午，我直接到邵阳市公安局双清分局国保大队办公室，将它交给了该大队的一位负责人。我对他说：“以前我写过多篇揭露江的历史问题和腐败问题的文章，你们也都看过，但都是零零碎碎的。这次我把江的历史问题概括为‘二奸二假’，完整系统地把它写了出来，事实清楚，一目了然。这篇文章很重要，最大的问题不仅仅是江个人有严重的日伪汉奸、苏俄奸细、假地下党员、假烈士子弟的‘二奸二假’历史问题和政治诈骗问题，更重要的是这样一个‘二奸二假’的骗子、坏人、卖国贼，不仅当了伟大、光荣、正确的中国共产党的最高领袖，而且一当就是十多年，接着他又横行霸道地大搞‘枪指挥党’，为所欲为地骑在党和胡总书记的头上大当‘太上皇’，甚至直到现在还在排斥胡而想插手十八大接班人的人事安排，简直是嚣张狂妄、毫不收敛、不要脸到了极点。可是对他如此胆大包天、胡作非为地祸害国家挟持党，却似乎没有办法加以制止，这也更加突出地表明这个混进党内篡夺大权的‘二奸二假’大骗子大奸贼对党和国家，甚至对整个中华民族的严重危害性。因为这件事情关系重大，究竟是在保密的情况下由内部调查处理呢，还是公开出去让全国的人们都知道这件事，都知道江是一个什么样的人，然后在广大群众的知情和参与下由中央组织对江的调查处理呢？这需要请示党中央，看看怎么办。所以这篇文章我写好后没有给任何人看，更没有寄发给别人和上网传播。现在我把它交给你们，是想委托你们通过组织系统，直接上交给公安和国安的领导同志，并呈送给胡锦涛、李长春、习近平、贺国强、周永康等党和国家领导人，请他们过目审示，然后给一个明确的答复。这篇文章你先看看，如果你们不能帮我递交给胡锦涛和党中央，那我就拿回去通过其他渠道递上去。”<br />
&nbsp; &nbsp;&nbsp; &nbsp;<br />
<br />
&nbsp; &nbsp; 这位双清分局的国保负责人即就接过文章看了起来，可能是文章的内容使他感到吃惊，所以看得很是投入和认真。他一口气看完后对我说：“这篇文章比你以前写的要完整系统多了，的确很厉害，很严重，这可是真正的头号国家大事了。这样吧，老吕，我马上拿去向上级汇报，并且通过组织系统直接上交给省厅和北京，按照你的要求呈送给胡总书记、李长春常委、习近平副主席、贺国强书记和周永康书记。请你放心，这么重要的事，我们一定会很快送到，绝对不会有半点‘贪污’的。不过你要答应我们的一个要求：在上面没有答复之前，这篇文章千万不要给任何人，尤其不要上网传送发布。”我答应说：“这当然，我本来就没有给过别人，甚至连我的儿子都不知道，写好后我马上就来交给你们请示中央指示了。”<br />
&nbsp; &nbsp; 过了两天，即12月3日上午9点多，这位双清国保负责人打电话给我，要我马上到他那里去，说是上面已经有了回音。我急忙到双清国保大队，这位负责人很认真地对我说：“老吕，你的这篇文章我们局里的领导都看过了，都觉得这件事情的确非常严重，关系重大，非同一般，所以都非常重视，并且马上给你直传了上去。我们也都想知道上面是什么态度，会做什么答复，这两天我们还用特别专线多次向上面询问结果，今天总算有了答复。”我急忙问：“是什么答复？”他回答说：“你的这篇文章中央领导看过以后都感到非常震惊，甚至可以说是特别的震动，你真是把天捅了个大窟窿了。由于事关重大，所以上面认真研究后很快就作了答复，上面的这个答复是一级一级传下来的，答复很简单，只有三个字：‘不答复’。”我马上问：“‘不答复’？这叫什么答复。”他回答说：“‘不答复’的答复，就是明确地告诉你，上面对你的这篇文章和要怎样去搞不表态。”我就问：“这个不表态的答复，是不是说这篇文章可以让我公开出去让大家都知道呢？”他回答说：“上面没有这样说。”我又问：“是不是不让我把文章公开出去，不让搞江的问题呢？”他回答说：“上面也没有这样说，只是说‘不答复’。”我再问： “那么这个‘不答复’的答复到底怎么理解呢？是让我搞呢，还是不让我搞呢？”他听后说：“老吕，你一向很聪明，这次怎么犯糊涂了？‘不答复’的意思显然就是这篇文章是你写的，你要文责自负，你要怎么搞，是公开出去还是不公开出去，全由你自己决定处理，上面不表态，不管。”我又说：“那么可不可以这样理解，实际上这是默认了我的这篇文章，默许我可以公开地去搞，所以对我来了这个‘不答复’的答复。”他说：“上面这样答复，可能是这个意思吧，不过我只能给你原封不动地传达上面答复的这个‘不答复’三个字，其他的我就不能向你多说了。”我又问：“这个上面是谁？是胡锦涛总书记，还是政法委周永康？”他说：“你就别管具体是谁了，我们下面的也不好多问，反正这个‘不答复’的答复是上面来的。不过你想呀，你这个人我们邵阳市局管不了，湖南省厅管不了，公安部也管不了，只有中央最高层那么几个人在管你，这个上面是谁你还不清楚？还要这样明知故问？”我想了想后说：“既然中央以‘不答复’的答复默许我去搞江的问题，那我可就真的要‘文责自负’地公开大搞了。”他回答说：“这就要你自己决定了。不过老吕，说句实话，你刚被送回邵阳来时是不许你再写反江文章的，而现在上面不仅不禁止你写这样的文章，而且还以‘不答复’的答复作了默认，这确实是一个很大的进步呀，这个大变化如果不是胡总书记的支持，那是不可能发生的，所以原先我们都为你捏把汗，现在连我们局领导都非常高兴。至于你要怎么搞，由你自己掌握、自己负责吧，我奉上级指示只能给你说这些了。作为下级，我们是不能按照自己的意思去随意解释上面指示的，只能不走样地照本宣科，原话传达，只要你准确理解就行。”我对他说：“江掌权的时候我都敢公开地揭他反他，现在他已经退位下台不是党和国家领导人了，胡锦涛已经大权在握占据主动了，而这次对‘二奸二假’又有中央‘不答复’的默认许可。”<br />
&nbsp; &nbsp; 于是，在这次谈话以后我即对“二奸二假”一文作了些修改补充，12月5日定稿后除把它重又交给双清分局这位国保负责人请他再次上递外，又立即复印了500份开始邮寄发送。发完后因为不够，又再复印，作进一步的广泛发送。通过邮寄发送的有：中央相关部门和领导、全国所有省、直辖市的省委市委书记、所有的中央级和省市级重要报刊杂志和一些电视台、北大等全国不少大学、我所认识和不认识的、与我有来往联系的熟人、朋友、网友、离退休老干部、老教授、老同学、一些军队单位和军队老同志等等，一个月的时间里共复印和寄发了1000多份。在此寄发的过程中邮道畅通，公安国保方面没有加以干涉、阻拦和卡扣。 2010年1月3日，为了扩大传播范围和影响，我又将“二奸二假”以及长达五万多字的解读十七届四中全会即“江胡斗”一文通过电子邮件上网传送，国外的华文网站和报刊对“二奸二假”都作了转载，这使传播的范围更广、速度更快了，全国和世界都知道了江的“二奸二假”历史问题和政治诈骗问题了。后来向我要此文的人越来越多，我就把这两篇文章又各复印了1500多份向全国各地邮寄，要不是复印和邮寄的费用太大，我将印得更多，寄发得更广(我原计划准备印发 5000份以上)。而据我所知，很多收到、看到和通过其他渠道得到这篇“二奸二假”文章者，不论左派右派民主派，不论平民官员和军人，也不论是城镇乡村的人们，都震惊不已、争相传阅、奔走相告、纷纷议论、愤怒斥江。有的人往往几十几百份地自行复印散发，并且要求他人也这样印发，有的则大量上网传送，有的大学以寝室为单位集体复印，北京和全国各地的许多大学、军地单位、老中青和社会各阶层各派别的人们都知道了，看到了，甚至不少偏远的地方也都在纷纷传阅扩散。在我广泛邮寄传送的半年多时间里，来电话来信支持和称赞者，以及向我索要文章者，连续不断，接踵而至，有的一要就是上百份。<br />
&nbsp; &nbsp; 这篇“二奸二假”之所以会引起朝野各界如此的重视、欢迎和热传热议，之所以不同立场观点的人们在反江问题上会如此的一致，除了因为江这个奸假骗贪独裁者实在太坏而为人神共愤、全民不齿、人人唾骂外，更重要的是它所揭示的问题是与每个中国人、甚至每个国家都有关系的大事。而这篇文章如果不是得到以胡锦涛总书记为首的党中央这个“不答复”的答复的默认许可，要使它能在全国乃至世界得到如此迅速、广泛和长时间的传播，并且现在仍还在以几何级数的速度和规模扩大传播的范围和影响，是绝对不可能的，我也是不可能放手去这样做的。<br />
&nbsp; &nbsp; 至於说到我在“二奸二假”一文中所揭示的内容是不是不实之词的谣言问题，我想如果它真的是“捏造事实诽谤他人”的谣言而不是无可辩驳、真实可信的事实的话，中央肯定不会给我作这个默许的答复的。再者，揭江历史问题的文章我在2003年2、3月间在邵阳时就曾写过多篇，不久我三弟就从北京打电话给我，告诉说胡锦涛总书记派他的一位秘书到我父亲家索要我写的几本书和一些文章，要我立即给他。我马上要我在北京的小儿子到我在香山的住处拿了给我三弟和父亲，由他们交给胡的秘书呈送给了胡，而我也很快被允许再回北京了。 <br />
&nbsp; &nbsp; 2003年4月我从邵阳回到北京后胡又派他身边的秘书以看病为名到我父亲家，通过我父和三弟问我要一些我写的战略分析文章，然后由我三弟到我在香山住处来取了交给他。有一次这位秘书专门来要我写的所有有关反江挺胡的文章，这其中包括：《一个值得警惕的新动向》、《请给胡锦涛投一票》、《耐人寻味的国共两党“第三代现象”》、《江泽民江西永新棉花坪避难之谜》、《江泽民的历史和入党时间等问题应该调查》、《请再给胡锦涛投一票》等。我立刻把这些文章交给了我的三弟和父亲，由他们交给胡的秘书转呈给胡。而当时江还是掌握军队大权用枪指挥党的中央军委主席，无军无枪的胡还处在江及党羽的严密监视挟制之下，他关注和重视我的反江挺胡之举而派贴身秘书来与我联系，是一件非常危险的事情，一旦被江的耳目觉察发现，就会暴露自己的反江意图而被江抓到可以害他的口实，因此胡的秘书因怕到我父亲家里时有人跟踪、谈话有泄，每次来时经常特意把我三弟叫到他的小车里作避江耳目的单独交谈，传达胡的一些意见、要求和指示，然后再由我三弟到我处转告给我 (我三弟告诉说，为躲避江的耳目，不被江抓住辫子，胡要求他的这位秘书以看病为由到我父家，只通过我父和三弟与我作间接联系，避免与我直接接触。我父亲是抗日初期中共上海老地下党员、新四军老战士，平生最痛恨日本侵略者和汉奸卖国贼，他本来就对江凌驾于党之上和大搞贪污腐败欺压人民的种种倒行逆施极为不满，在得知了江父子是日伪汉奸、江是冒充的假地下党员、假烈士子弟并且出卖150多万平方公里的大片北方国土的情况后更是怒不可遏、恨江反江，所以非常支持我挺胡反江，并为能得到胡总书记和一些与他同辈的老革命老干部的直接支持感到高兴，不仅提供联系条件，而且还对家人说：“我们吕家又要对中华民族作贡献了!”)。2004年 2月我揭江丑闻的《反映信》也是为了向胡和中央反映汇报江的贪腐问题以便对江调查才写的。可见我的这些揭江之文不仅不是谣言，恰恰反倒是提供了江的问题而可以对江进行调查的重要线索和证据，并且已经引起了胡锦涛总书记和党中央的高度关注和重视。<br />
&nbsp; &nbsp;&nbsp;&nbsp;这引起了江的极度恐慌和仇恨，他和亲信后来企图通过对我反江挺胡行动和我与胡联系情况进行卧底盯梢、跟踪监控和威胁逼供来追查我和胡的关系，设法找到借口设置陷阱来打击陷害胡，所以他们搞我整我主要是为了把矛头指向胡，甚至准备借此逼迫和搞倒胡，尤其在2003年12月我父亲去世后他们即就准备对我下手。但他们的阴谋没有得逞，在2004年2月当胡得知我因写了揭江《反映信》受到特工跟踪而逃跑时，就把当时还是公安部长的周永康叫去严厉训斥道：“你们是怎么看的吕加平？他是怎么失踪的？!吕加平如果出了事，我就拿你是问! 无论如何要尽快把他找回来!”也持反江立场并与我父关系很好的乔石以及李瑞环、尉建行、宋平、万里等党和国家前领导人也纷纷出面阻止江和江派对我谋害，所以尽管后来我受到了数年的严密监控而吃了一些苦头，但江和党羽们却始终不敢对我下毒手。<br />
&nbsp; &nbsp; 更使江害怕的是，从2002年7月到现在的整整八年中，我的揭江文章、尤其是这次的“二奸二假”，不可阻挡地在广大民众中、在国内外网上广泛流传，已经形成了滔滔的反江浪潮而使江心惊胆战、惶惶不安。而江和护江者们除了无理无据地给我和支持者强行扣上一个所谓“捏造事实诽谤他人”的空帽子外，却也从来没有对我的包括“二奸二假”在内的揭江文章进行过有理有据的反驳、“辟谣”和起诉。既然江本人对我揭发他的“二奸二假”问题始终拿不出证据而没有办法可以证明这是谣言，始终不敢进行起码的驳斥和“辟谣”，更不敢向法院起诉告我，那么其他护江者对我和支持者的所谓“捏造事实诽谤他人”的诬蔑攻击又怎能站得住脚呢？即使他们花费再大的心计和力气，使出种种花招和手段，又怎么能把江这个“二奸二假”的坏人、骗子、卖国贼，漂白打扮成为干净无暇的好人、善人呢？而如果我这是真的在“捏造事实”对江诽谤造谣的话，他们会放过我吗？肯定早在多年以前就会被大权在握的江和亲信们以我造谣为由抓起来置于死地了，这也正如公安方面有人曾对我说的：如果没有胡总书记和老同志们对你力保，你吕加平的脑袋早就掉过好几次了。现在拖了这么多年还没有对我实行他们的牢狱专政，更没有把我搞死，这不是也反过来证明我对江所揭发的内容绝对不是谣言而恰恰是事实吗。 <br />
&nbsp; &nbsp; 假的就是假的，伪装必须剥去。奸假骗贪黑五毒俱全的江是永远漂不白的，护江保江者费尽心计地企图绕过他的“二奸二假”黑核心而想把他漂白成为好人、善人甚至“伟人”，也根本绕不过去做不到的。尽管他们如此深情和卖力地百般袒护、开脱、掩饰和包庇江，还想用恐吓、威胁和专政手段企图阻止广大群众对 “二奸二假”的散发传播热潮和对江抨击声浪，甚至还在处心积虑地把矛头指向以胡锦涛为总书记的党中央，然而这种继续反党反人民的保江护江最后挣扎，反而会越压越传、越传越多、越传越广，反江怒火会越激越大，而且也会更加暴露自己与江的死党关系而“搬起石头砸自己的脚”地去做奸假骗子江的殉葬品。 <br />
&nbsp; &nbsp; 父母辈打日本浴血抗战八年，儿孙们斗汉奸拼死揭江八载。前扑后继无所惧，天翻地覆慨而慷，宜将剩勇追穷寇，不可沽名学霸王! <br />
&nbsp; &nbsp; 我相信，今后“二奸二假”一文将会得到更加广泛的传播扩散，在中国真正做到家喻户晓、人人声讨。而且到时候一定会有人、甚至会有很多的人挺身出来，公开发起要求中央组织有各阶层代表参加的调查江“二奸二假”问题和贪腐问题的全国性“群众请愿签名运动”，以胡锦涛为总书记的党中央也一定会顺应党心军心天下民心和社会民主潮流，旗帜鲜明地全力支持这场查江反腐群众运动的。 <br />
