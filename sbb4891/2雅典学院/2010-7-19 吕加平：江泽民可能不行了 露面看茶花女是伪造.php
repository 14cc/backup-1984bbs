<br />
早在2003年，胡锦涛就派贴身秘书以看病为名去吕加平父亲家中联络，专门要他写的反江挺胡的文章。胡的秘书因怕有人跟踪、谈话有泄，每次去时经常特意把吕加平的三弟叫到他的小车里作避江耳目的单独交谈，传达胡的一些意见、要求和指示，然后再由其三弟到吕加平处转告给他本人。吕加平的三弟告诉他说，为躲避江的耳目，不被江抓住辫子，胡锦涛要求他的这位秘书以看病为由到吕父家，只通过吕父和三弟与吕加平作间接联系，避免与他本人直接接触。<br />
<br />
据吕加平自述：“当时江还是掌握军队大权用枪指挥党的中央军委主席，无军无枪的胡还处在江及党羽的严密监视挟制之下，他关注和重视我的反江挺胡之举而派贴身秘书来与我联系，是一件非常危险的事情，一旦被江的耳目觉察发现，就会暴露自己的反江意图而被江抓到可以害他的口实。”<br />
<br />
中国共产党的总书记让他的贴身秘书去联系一个基层党员，向他传达总书记的重要指示，去向外界揭露中共军委主席日伪汉奸和苏俄内奸的真相，这现实中的一幕不是比所有的谍战片更惊险，更刺激，更具戏剧性的“暗战”吗？<br />
<br />
