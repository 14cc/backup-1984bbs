																											<div id="postmessage_747620" class="t_msgfont">一个说江在6月6日病重入301，一个说江在6月8日身体绝对健康地到国家大剧院观看法国人演出的世界名歌剧《茶花女》，而且精神焕发、神采奕奕地上台与指挥、女主角和演员们合影留念。这是两则截然相反的消息，人们不禁要问这是怎么回事呢？这两则消息之中一定有一个是假的，那么究竟哪一个是假的呢？本人出于好奇与疑惑，便仔细观看和琢磨这张江到大剧院看《茶花女》的照片，结果发现该照片有许多疑点和伪造的痕迹：<br />
<br />
1、国家大剧院的舞台面积很大，左右和前后纵深的空间宽阔，由于在舞台后面和两侧没有墙壁，因此不可能装有壁灯，江等上台与演员合影，其舞台背景和两侧应还留有演出布景和侧幕，但从照片上看，不远处的后面却是墙壁，并在壁上有两簪还亮着的长条园柱壁灯。由此可见，这个舞台的纵深空间很小，根本不是国家大剧院的大舞台，而很可能是一个面积和空间不大的小会议厅主席台或小演出厅小舞台；<br />
<br />
2、从2009年12月29日江参加阿沛&#8226; 阿旺晋美追悼会时来看，他的头发已完全白化且稀疏，但这张照片中江的头发却还多有黑发，可见这是他以前的留影，而不是现在的模样；<br />
<br />
3、李长春现在已明显老态，其脸颊面孔也比过去要胖一些，但这张照片中的李长春却比现在要年轻许多，脸面也比现在的要瘦一些，足可见这是李过去的影像而不是现在的样子；<br />
<br />
4、这张照片中最突出显目的是在照片正中站在李岚清和江泽民之间的是身穿平时服装并且没有演出装扮的《茶花女》女主演玛丽&#8226; 邓立维，根据演出和接见的常识，演出结束后领导们上台接见演员并与他们合影，肯定是在演出刚刚结束之后进行，这时所有演员在舞台上集合列队，等大幕拉开，由鱼贯上台的领导们同他们一一握手问候并列队摄影，因此演出结束时演员们是不可能也没有时间可以离开舞台到后台去卸妆的，而只可能仍以演出全妆打扮接受上台的领导们接见和摄影的，所以这位《茶花女》女主演在演出结束后不可能也没有时间可以到后台卸妆，然后再出来到舞台中央接受接见和拍照，只可能一俟演出结束时以演出妆扮服装接受江等的接见和拍照。可是这张照片上的《茶花女》女主角玛丽&#8226;邓立维却是以卸了妆的平装身姿与江等合影，她怎么可能在演出结束后和马上就要受接见前能有时间到后台去卸妆呢？这是绝对不可能的。因此这也证明所谓江在6月6日晚到国家大剧院看《茶花女》演出并接见女主角等是编造出来的，这张照片是以电子合成剪贴而伪造出来的；<br />
<br />
5、网易在公布这照片时说，这是一位携女友看演出的观众所拍，然而这位普通观众有可能到离江和领导人及演员们这么近的距离(甚至可能在舞台上)去拍这张照吗？众多的保卫人员不会阻拦他靠近领导人而会让他前去自由拍摄吗？这显然是不可能的。<br />
<br />
再者，从这位女主演的视线、神情、打扮、站位等来看与整个照片中所显示出的演出和接见气氛、格调和照片色调极不相符相称，使她完全游离于照片当时的环境和氛围之外。可以明显看出这是有人把该女主演过去的照片影像剪下来贴上去的痕迹，而所有的人都把视线集中于前面的摄影师进行合影时，她却分心地回过来注目看这位观众摄影者，也就是挤上前去拍照的这位网友，这显然也是不可能的；<br />
<br />
6、中央有明确规定，凡中央常委的公开活动，都要在当日或近日予以公开报道而见诸于报刊、电台和电视等各种媒体，这次网易透露的6月6日有江泽民出席观看的国家大剧院《茶花女》演出中从照片中可以看到出席者中有现任常委李长春，但当日和近日的所有媒体却都没有对李长春观看这次演出的报道。媒体为什么不对李长春观看这次演出作出报道呢，这是大大反常的，这也可以证明并没有李长春陪江泽民去大剧院看《茶花女》之事。<br />
<br />
除以上所挑出的几处造假痕迹外，照片中还可能有一些本人没有发现的造假可疑之点，但仅以上所列的六条，就已经可以基本判断认定这张照片是用电脑合成拼接剪贴出来的伪造之物，江在6月6日晚根本没有在李长春、李岚清、刘琪等的陪同下到国家大剧院观看什么《茶花女》，也根本没有上台与指挥、女主角和演员们合影，这张照片和这条消息报道都是假的。实际上这可能是一张几年前江等在某小礼堂或小会议厅观看其他一次什么演出和上台接见时所拍摄的照片，其中现在被玛丽 &#8226; 邓立维影像所剪贴占据的位置，很可能是那位被他宠爱的将军女歌手。<br />
<br />
现在的问题是，究竟是什么人在盛传江已重病入301之际，尤其在胡锦涛离开国内前往中亚访问的空档之时，敢于和能够翻出这张属于绝密级的照片而采用的换人手段和编造之词造此造谣之假呢？特别引人注目的是6月10日晚香港凤凰电视台又特别报道了江在6月6日晚到大剧院观看《茶花女》的这条假消息，这使得这个谣言传播得更广了，也使人们更加相信江出来是真的了。既然现在只有靠造假来骗人相信江非常健康地出来看演出，这岂不正是“此地无银三百两”地证明他已经真的出不来了吗？这又是什么人能够如此胆大妄为地指令香港凤凰电视台老板这样做的呢？他们跟这些照片和消息造假者之间究竟存在有什么密不可宣的幕后黑暗交易关系呢？我想，具有火眼金睛般高超侦察识别能力的国安国保侦探部门是一定能够把此造假的谣言者追查出来予以辟谣并严厉惩处的。而且既然现在只有靠造假来骗人说江非常健康地出来看戏，这不正是“此地无银三百两”地证明他确实已经出不来了吗。<br />
