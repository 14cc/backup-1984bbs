<br />
胡锦涛的团派和江泽民的上海帮之间的刻骨仇恨是用尺子都量不清的。当年邓小平当着江泽民的面说好了胡锦涛将来接他的班，这是党政军上上下下都知道的既定方针。谁知邓小平一死，江泽民就翻脸不认人。非但在党内和军中大肆拉帮结伙，遍植党羽，极力扩张其上海帮的势力，而且处处压制胡锦涛和他的团派，不让他们有出头之日。在江泽民得势的十几年里，上海帮天天都是好日子，处处闷声大发财，却让胡锦涛他们坐冷板凳，喝西北风。<br />
<br />
胡锦涛处处小心低调，如履薄冰，好不容易熬到了十六大出头之日，谁知江泽民又横生枝节，指使当时的军委副主席张万年等军头在十六大上搞出一个“特别动议”来，用枪来要挟党，迫使党中央同意让江泽民继续留任军委主席，垂帘听政，此举无异于军事政变。虽然没有流血，可是所造成的后果比流血更严重。因为这意味着江泽民在过去的十几年里滥用军委主席的职权，以高官厚禄和放任腐败为代价收买了军中一批将领，已经让解放军从中共的党卫军转变成上海帮的蛊惑仔了。<br />
<br />
此后胡锦涛为了夺回本该属于自己的权力，不得不使出九牛二虎之力，在嫡系团派的帮助下在党内拉出一条反江统一战线，终于把江泽民赶下军委主席的宝座。谁知那个老不死的又因此把胡锦涛恨之入骨，非但指使曾庆红、陈良宇等上海帮干将对胡锦涛大搞抢逼围，使他政令不出中南海，而且多次企图致胡锦涛于死地。<br />
<br />
2006年“五一”期间，胡锦涛赴青岛视察海军，乘驱逐舰在黄海视察海军演练时，他的座舰忽然同时遭两艘海军驱逐舰机关炮扫射。胡锦涛身边的五名海军士兵被打死，胡本人险些丧命。2007年10月，保卫部门在胡锦涛下榻的上海西郊宾馆地下车库内发现在食品专用车的司机坐垫下藏有2.5公斤装有定时器的烈性炸药。中共军队已是上海帮的蛊惑仔，上海滩更是江泽民的老巢，无论是从刺杀动机还是作案条件来看，这些欲致胡锦涛于死地的谋杀只能是江系上海帮之所为。<br />
<br />
至于去年的中共建政60周年阅兵式，本该是胡锦涛这一生中最荣耀、最辉煌的时刻。可是由于那个无官无职，早就是一介平民的江泽民故意抢风头，结果本该是军委主席胡锦涛独领风骚的历史时刻变成了江胡二人转，这无异于向全党全军、全国人民甚至全世界证明了中共党内和军中存在着两个互不相让的核心。中共过去的党史表明，凡是党内和军中出现了两个核心的时候，你死我活的权力斗争必然随之而来，最终必将以一个核心消灭另一个核心而告终。这是共产党与生俱来的本质所决定的，是不以人的主观意志为转移的，从列宁、斯大林到毛泽东、邓小平，莫不如此。<br />
<br />
在年初发表的《胡氏树孔子，团派隳三都》的文章里，存中剑就点出了胡氏团派围剿江系上海帮的三大主攻方向。其一是地方势力；其二是政法系统；其三是军队。随着独霸新疆十五年的“新疆王”王乐泉的倒台，以及各位团派省委书记的全面上位，江系地方势力中最顽固的堡垒已经被团派攻破，剩下的不难传檄而定。而薄熙来对文强所下的杀手，也为胡锦涛所利用，成为他借此敲打政法系统，威慑各地公安局长们的致命武器。要知道现在中共官场已经几乎找不到不涉黑的公安局长了。<br />
<br />
文强被处死刑一事沉重打击了周永康的威信（文强最嚣张的那几年周永康任公安部长），也给了胡锦涛整治政法系统的最好理由。最近中共搞的“严打”，其中很重要的一个内容就是要严打涉黑警察。显然胡锦涛是要借此削弱周永康的权势，树立自己的权威，“规劝”公安系统的人马重新站队，和团中央保持高度一致。文强一死，各地公安局长们为之胆寒，在当今盛行看风使舵的中共官场，上海帮的政法系统这个山头铁定是保不住了。<br />
