													<h2>儒教的性善论是阻碍宪政建设的最大障碍</h2>
						
						
																											<div id="postmessage_445832" class="t_msgfont">为什么要宪政？我以为，宪政的目的就是为了限制当权者乃至国家政权的权力。因此，对于人性善恶的判断对宪政能否有效建立至关重要。事实上，作为一种错误的人性观，性善论对于宪政的建立具有极大的危害。性善论会混淆人们对宪政的理解，误解宪政的意义，不明白宪政建立对民主社会建设的必要性。在性善论指导下的民主宪政体制，必然会沦为北洋时期的胡闹民主，流氓民主，而且，这样一个没有完善宪政体制的极端松散民主制度，行政效率必然低下，导致民众对民主制度的不满，并有可能给予TG性质的邪恶组织以复辟法西斯专制体制的可能。<br />
我们应当认识到，TG的垮台只是时间问题。中国千年以来的历史上没有一个专制政权可以存在到两百年以上，而TG执政无能，捣鬼有数，经济建设一塌糊涂，贪腐势力极端膨胀，TG内部各权贵集团争权夺利，内斗不断，人民对TG的不满与日俱增，社会矛盾不断激化。这些现象都告诉我们，只要耐心等待，TG必将有垮台的一天！然而，即使这一天真的到来，我们也不要太过兴奋，在中国两千年专制文化熏陶下，在TG六十多年的极权统治下，社会的民主素养极端匮乏，而在教育界牢牢把持在TG掌握的现状下，对广大民众进行民主化教育及政治素养的培训无疑是不可能的。因此，我们只能寄希望于宪政制度的建设上，而要想建立符合国情，具有可操作性的宪政制度，对人性的正确认识是必不可少的。<br />
儒教的主流思想正是“性善论”，这一点孟子已经明确的肯定了。而孔子虽然没有提过这个问题，但从其“圣王”论，追求贤人治国的政治思想和主流儒教长期对主张性恶论的“异端”荀匡的打压来看，其无疑也是性善论的支持者。就对理想社会的设想来说，儒教可谓与邪恶的马克思主义异曲同工（马克思主张：在工人阶级的先锋队――共产党带领下，建立共产主义社会；儒教主张：在“圣王”的带领下，建立“有饭同食、有衣同穿、有田同耕...”的“大同”社会）。这也正是为何马克思邪教会在中国风靡一时，吸引大批知识分子信徒的深层次原因（例如儒教分子杨度，董必武等），也是五四运动没有铲除儒教邪说在中国思想界根基的明证。<br />
儒教为什么要鼓吹“圣王论”呢，其本质无疑是“性善论”的体现，儒教分子认为，人性本善，人人可成尧舜。既所谓“六亿神州尽舜尧”。因此，皇帝也有可能经过教化而成为“明君，贤主”来带领广大臣民进入儒教所设想的“共产主义”式大同社会。因为儒教的圣人是完人的化身，不会犯任何错误，所以对皇帝，只应教化，不应过度监督。致使儒教徒对于皇权的制约始终没有实质性的进展，除了不疼不痒的’天人感应“论外，一个制约皇权的宰相制度能用上千年，期间随着皇权的不断扩大而几乎沦为虚设。<br />
虽然儒教另一位首脑荀子正确认识到了人性本恶的真理，提出了“人之性恶明矣，其善者伪矣”这一真知灼见，奈何日后儒教主流对其进行排斥，使其长期成为儒教中的异端思想，因此，荀子思想并不能代表儒教，更没有影响到儒教分子的实践，改变儒教主流认知――人性本善。<br />
人性本善论的对错是一个极为深层次的问题，在理念上纠缠并不能说明什么。然而，看看儒教分子们的实践结果，对我们正确认识性善论对错无疑是有作用的。事实上，从西汉已来，绝大多数帝王都是些凶残暴虐、荒淫无耻、贪鄙庸碌的家伙，跟“圣王“基本挂不上边。然而，儒教徒在”圣王“论的指导下，依然极力维护这些恶棍暴君，徒劳的对这些专制流氓们进行几乎毫无用处的”教化“<br />
翻翻中国历史，我们将非常清楚的看见，中国历史上的帝王绝大多数是恶棍或小人，即使有李世民、赵匡胤等几个贤明的君主，开明的统治也维持不了两代人，更不用说靠他们实现“大同社会“了。不难判定，儒教的“圣王“思想在实践中彻底失败<br />
儒教的“圣王“思想为什么在实践中彻底失败了?其根源，就是对专制君主没有一个正确的认识，没有创立对专制君主的权力形成全面制约的方案。因为在儒教分子看来，身为臣子的自己去制约君主的权力，是违背”君君臣臣“，尊卑有序的大逆不道之举。<br />
儒家相信人性本善。既然人性本善，那么对专制君主只要施以教化手段就行了，用不着施以制约手段。这一错误的思想，导致儒教成为中国封建君主制度的帮凶，狗腿子。至于当代某些说儒教无辜儒教分子的理论更是不值一哂，为什么中国历代封建君王没有选择其他思想，而单单选择你儒教作为其对臣民进行思想统治的工具呢？最早的封建思想专制意识形态“罢黜百家，独尊儒术“为什么是一个儒教徒董仲舒提出的呢？如果主流儒教反对这种思想，为什么要把董仲舒列为”圣人“阵营，大加供奉呢？如果儒家本身没有迎合专制统治需要的独到“优势”，专制帝王怎么会对儒家青睐有加？儒家迎合专制统治的特点不难窥破。<br />
中国为什么靠自身努力走不出专制――造反――新专制的怪圈呢？除了儒教误国，还有别的解释吗？儒教思想的“大同社会“无疑让中国人更加容易接受共产党独裁统治，而其”圣王论“，又为TG的专制独裁奠定了思想基础。――文革时响彻天际的”中国人民大救星“就是明证。而韩国、日本的宪政民主体制由美国直接强力改造而成，台湾的民主虽然不是由美国直接建成的，却是在中共武力的严重威胁和美国的巨大压力下成就的，蒋经国主导的民主化转型，实质上是迫于情势的不得已之举，这些都不能作为”儒教与民主制度并行不悖“的证据。<br />
儒家必然会成为专制的卫道士，因为儒家认为人性本善，认为人性本善就必然会走追求圣王的道路；追求王的道路就必然是追求人治的道路；追求人治的道路就必然导致专制独裁。<br />
因此，人性本善论根本就是错误的。为什么错误？因为人的本能就是获取食物与能量来维持生存，所以人的活动必然带有一定的排他性，这就是人的自私本性的根本来源。人因为有自私的本性，所以容易做坏事，因为做坏事往往能损人利己，满足自己的生理欲求；而做好事往往需要损己利人，有碍于生理的需要。凡人因为本性自私，私心或者情绪一上来，很容易做坏事。一般情况下，普通人做了坏事，只能危害到其他的个别人，但是，掌握权力的人做起坏事来，就能产生普遍的危害，权力越大、危害越大，因此，非要对执掌政权的人的权力进行限制不可。这正是与儒教“圣人治国“相冲突的。<br />
