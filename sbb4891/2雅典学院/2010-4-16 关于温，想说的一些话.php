<br />
在当今的环境下，此文发表绝不那么简单，或许有一天可以有一些证据佐证俺的这个观点。<br />
ZG向来是强调在某件事情上铁板一块、无缝钢管的，但此次直觉不是这样，从各大网站大量跟帖来看，民众的民主情绪又一次被调动了起来，这种不良后果，你们以为是温及巨头们想像不到的？这更佐证了此文刊发不知道经过了何种博弈。<br />
俺对体制非常了解，所以太清楚在这种语境中什么是最敏感的了。如果不是温写这个文章，一般省一级高干连想都不敢想这个问题。温开篇的一句话，“胸中那积蓄多年的怀念之情如潮水般起伏涌动，久久难以平复”，请勿忽视，这是走钢丝的一句。他为何如此犯险暂时不得而知。正如上述跟帖转引外电说法，这是玉树地震外的另一场不大不小的地震，可能没有余震，但地震已经客观地在人心和舆论中发生了。今天我们单位很多人都在议论这篇文章。这是客观的效果，不是哪个人编造的。<br />
温的此文，体现了冒险和稳健的某种平衡，因为按照ZG政治游戏规则，在下级对上级要绝对忠诚的标准下，对提携恩师的尊重纪念是一种几乎不容冒犯的潜规则和政治正确，我们可以看到即便一些人贪污腐败锒铛下狱，昔日下属还有很多去探望抚慰，何况胡乎。冒险的因素却在于，胡的身上体现着某种象征，而这种象征不是哪个人赋予的，是客观存在的。<br />
<br />
如果说政治良心之类的话，容易被犬儒盛行说成是幼稚，那么俺还是提醒――除了实利追逐，请勿漠视人心中还有另外的、可能沉睡但不可能消灭的人性的东西，无论他是一名乞丐还是国家总理。否定这一点，就是否定了社会尤其是我们这样一个社会通往自由的任何人性根基。所以俺总是不清楚，否定现制度和TG之后又否定一切，包括事件中任何人性表露的诸位，为何整天在此处谈论建设自由社会？犬儒通不向自由，你们表露的犬儒心态证明了胡平先生90年代的论断：愤世嫉俗的下一步即是玩世不恭。<br />
“犬儒不只是在过去饱尝辛酸，犬儒是对未来过早地失去希望。”（J・ 哈里斯）<br />
“人们变得玩世不恭，‘看透一切’，政治冷感，即犬儒主义。”（胡平）<br />
<br />
温的家眷。。。。。。许多事情无法辩护，也不可能辩护。<br />
但希望标榜自由的人持这样一种认识：人性的复杂远远超过你我的理解。站在道德和经济主义高端来像上帝那样透视一个人，自认为高明，可能恰恰是无知的表现。或许有一天我们会发现温的隐秘的人格分裂和复杂性。<br />
<br />
俺从来没有把未来走势寄托在某一个人身上。还是拿句话，未来靠当前一点一滴的努力。但是不要忘记，前苏联的变化是从民众包括军队唾弃开始的，但却是从何处启动的。<br />
<br />
对自己的言论不作辩解了。这里都是高级深刻的人士，都是看穿一切的精英，但你们的夸夸其谈和坐而论道，你们的脱离素朴和基本常识（包括政治常识），你们的粗鲁犬儒，只能让这个顾问中冠以许多俺尊重的国内自由主义者名字的地方蒙羞。<br />
