<br />
&nbsp; &nbsp; 泰兴幼儿园中的小孩也被人砍了，32人受伤，死亡情况不明。这个新闻因为离开上一次南平幼儿园袭击的新闻太近，我甚至一度误以为是同一个幼儿园。<br />
<br />
&nbsp; &nbsp; 在最近的变态凶手杀人事件中，他们都选择了幼儿园和小学，相信在很多想报复社会的人心中，去幼儿园小学杀人成为了一种时尚，因为在杀人过程中，你将遇到最少的抵抗，杀掉最多的人，造成民间最大的痛苦的恐慌，是最有效的报复社会手段。除了杨佳以外，几乎所有杀手都挑选了向弱者下手。这个社会没有出口，杀害更弱者成了他们唯一的出口。我建议把全国地方政府门卫间里的保安们抽调去保护幼儿园，孩子都保护不了的政府不需要那么多人保护。<br />
&nbsp; &nbsp;&nbsp; &nbsp; <br />
&nbsp; &nbsp; 这些杀人事件的产生很大原因是这个社会不公正，不公平。是的，让公平正义比太阳还要有光辉。但太阳不是每天都出。我们的阴天和黑夜是否稍微太多了一些？所以，提出让公平正义比太阳还要有光辉并不伟大，做到让太阳分分钟都挂在你头顶上才伟大。<br />
<br />
&nbsp; &nbsp; 在泰州幼儿园杀人事件中，新闻被控制了，这些孩子们生不逢时，死更不逢时。在相关部门的认识里，在这喜庆的气氛里，这事当属杂音。我们只知道，泰州幼儿园杀人事件中，受伤32人，政府和医院一再强调，无一死亡，但是坊间又传说，死了多个孩子。你说我应该相信谁呢？相信政府吧，那为什么他们禁止家长见到孩子呢？至今还封锁着医院和新闻，没有孩子的照片和视频，况且一个杀人用刀劈了32个人，结果一个没死，那他到底是在杀人还是在做手术呢，也太小心了。相信传闻吧，毕竟传闻都是喜欢往夸张了传的，我们无图无真相，也不能相信。于是我一搜索泰州，出现的新闻居然是――《泰州近日三喜临门》，日期是4月30日。<br />
<br />
&nbsp; &nbsp; 我只是非常的诧异，泰州政府通过了封锁消息，封锁医院，控制媒体，禁止探望，转移视线，等手段，居然成功的将人们对于杀手的愤怒转移到了自己身上，这是何苦。你以为他有什么目的，其实不是的，除了要配合世博会《和谐欢歌》以外，这只是惯性，是政府处理类似事件的习惯，是七步曲：吃饭喝酒到一半，出事了――隐瞒，隔离，撤媒体，发禁令，发通稿，赔钱，火化――继续吃饭喝酒。他们处理问题的手段不比凶手高尚多少，也难怪在网上看到有幼儿园挂出横幅――冤有头债有主，出门左转是政府。<br />
<br />
&nbsp; &nbsp; 短短的一个多月内，五起校园凶杀案件，短短的一周以内，就发生了两起，4月29日，泰州，4月30日，潍坊。我不想去探讨其中的社会原因，只想告诉大家，也就在这里，一个人冲进幼儿园砍了32个小孩是不能上社会新闻的，32个加起来才超过一百岁的孩子，你们被砍了，连个报纸都不给你上，因为在几百公里以外，召开了一个盛会，那里光烟花就放了上亿，同时在你们的家乡泰州，要召开国际旅游节，经贸洽谈会和华侨城开业典礼，正三喜临门。<br />
<br />
&nbsp; &nbsp; 也许在那些爷爷们眼里，你们，是扫兴的。<br />
 <br />
<br />
&nbsp; &nbsp; 但是，我们可怜的孩子们，奶粉毒害的是你们，疫苗伤害的是你们，地震压死的是你们，被火烧死的是你们。就算是成人们的规则出了问题，被成人用刀报复的也是你们。我愿望真的像泰州政府说的一样，你们全部都只是受伤，无一死亡。年长者失职了，愿你们长大以后，不光要庇护你们自己的孩子，还要让这个社会庇护所有人的孩子。<br />
