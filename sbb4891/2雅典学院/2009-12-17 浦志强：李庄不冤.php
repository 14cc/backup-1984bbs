<br />
新近，重庆打黑事件进一步扩大，北京著名的康达律师事务所律师李庄被重庆检方以五项罪名抓捕至重庆。一石激起千重浪，李庄被抓，政法大学法学院副院长何兵在央视跃出，剑指薄熙来和王立军。北京20几名律师联名向北京律协申请律协介入保护李庄的合法权益。在大陆新闻界影响力巨大的“中国青年报”发出长篇报道，对李庄律师被抓进行了详细的背景介绍。此文与央视的节目形成了对抗之势。这不免给中国原来大一统的新闻圈带来了新的气象。在此，笔者不多加以论述。本人要说的是，富强和他的康达律师事务所。<br />
<br />
　　傅洋乃前全国人大委员长彭真的大公子，曾在全国人大法工委为官，因仕途不顺，下海经商，并和原最高人民检察院院长郑天祥之子郑小虎合办了“康达律师事务所”。此二位太子党在京城司法界可谓是翻手为云，覆手为雨的高手，很多在全国涉黑的大，要案都有此二人染指。北京民间有一说，傅洋和郑小虎是江湖上的打捞队队长（打捞队即专门以人际关系为手段，为各种刑事犯罪嫌疑人做无罪和由重罪变轻罪的法律服务）。说白了，就是你虽然有罪，但是只要你出钱，我就可以为你消灾解难，无论你是杀人犯，纵火犯，强奸犯，和经济犯。但打捞队从不涉及与政治有关的案件。傅郑二人也从不接国内各种异议人士委托的案件。他们只收出钱多的委托人的授权委托的案子，哪怕你是黑社会，哪怕你是政府的官员中的腐败分子。<br />
<br />
　　2002 年末，公安部刑事侦查局接到重要线索，航行在长江上的东方皇后和东方皇帝号的游轮有贩毒集团在游船上疯狂地进行毒品交易活动。公安部刑事侦查局秘密地调动警力，拟对由香港某集团出资控股的两条游船进行突检（该集团老板叫杭某某）。当警方欲上停靠在汉口码头的东方皇后号进行检查时，遭到了船上几十名保安的反抗，保安头目并召集了武汉三镇的几百名黑社会人员与警方交火，一时汉口码头枪声大作，警方和保安方各有伤亡。北京“法制文萃报”对此事件进行了报道，但报道的第二天该报纸被全部收回。事后，在傅洋的一手操作下，杭某将东方皇后号迅速转卖，此事公安部拟继续深入调查，被时任中央政法委书记的罗干叫停。傅洋让杭永明给罗干送了多少钱，时任湖北省委书记的俞正声一直保持沉默。这未免让人觉得有些心寒。<br />
<br />
　　发了大财的傅洋在北京怀柔县和昌平县购买了大面积的土地，拥有自己的农场和庄园，以及高级俱乐部。凡是有要捞人的请求，都要到其俱乐部，农场和庄园去拜见大哥大傅洋。在傅洋可后，李庄这样的马仔才有机会去操作大的刑事案件。<br />
