													<h2>浦志强：李庄不冤</h2>
						
						
																											<div id="postmessage_378905" class="t_msgfont">康达所律师李庄“捞人”失手，在政治正确的打黑运动中“偷鸡”，连累律师界从短道速滑变成高山滑雪，“美誉度”直逼中国足球。这就迫使北京律协从荣耻观高度，支持渝方查处行为，继之以谨慎表达维权美意，为此特遣资深刑辩律师衔命探营。组合拳出手，总算娘家人有了“维护”会员“合法权益”的意向。但律协只能先查事实――侦查阶段还不能放手查，赤膊上阵违法的事，把资深刑辩律师全打死，他们也不会做。“舍命陪君子”不是专业律师的套路，组织上更没这份义务。所以我相信，“捞出”李庄可能性微乎其微――对取证程序的合法性，李庄早在落马前就质疑过了，他用这招儿没能“捞”龚，后人照方抓药，估计也“救”他不下。<br />
&nbsp; &nbsp; 据《中国青年报》接二连三的独家报道，李庄似乎触犯了刑法第306条。倘若报道所言属实，他自然罪有应得，好在律协已声称一经查实将取消其执业资格。或许，轰轰烈烈的“打黑门”，将以热热闹闹的“律师门”收场。好比搂草打着了兔子，渝警在打黑除恶的关头，还赠送北京律师队伍一份免费的“保洁”。想当年牛群冯巩搭帮开过家点子公司，受托给蛋糕厂出个起死回生的点子，不期然救活了隔壁一家濒临倒闭的蜡烛厂――可惜蛋糕厂终究关了张。假如打黑终将无大成就，有了李庄的折戟沉沙，我们就只能说虽然脱靶，但枪手毕竟顺手把邻居家房顶揭去几片瓦，也算不虚此行“打哪儿指哪儿”。<br />
&nbsp; &nbsp; 李庄组建的“跨区域打捞队”，番号几何不得而知，但业内有“打捞”现象，却天地你我皆知。从上到下二十年来，大小“打捞队”不在少数，蔚为大观的也非寥寥。近水楼台先得月，江山代有才人出，“打捞队”各领风骚不几年，是因为雇主现实得很，看中的是你老子的影响，没人罩着你再资深也未必有执行力。谓予不信，等老子到点儿下台，越资深可能越惨淡――花天价找来律师，自会有人看中你身怀的绝技，但多大比例仅仅看中这些，你和他之外还有天知道。说句公道话，不见兔子不撒鹰是精明，逮着兔子不舍得撒鹰，就近乎无赖了。初级阶段各行各业没规矩，草创时期那种没本儿的生意积习难改，时迁儿们妙手空空的英雄本色，没有前途。<br />
&nbsp; &nbsp; 问题是“有奶便是娘”和“好死不如赖活着”，也是江湖好汉的座右铭。把谁放在龚的角度，保命都是压倒一切的目标。悠悠万事唯此为大，请律师为了活命，掀资深刑辩律师的屁帘儿，多半也是为了保命。“青山依旧在”度过这一劫，保外减刑“值得期待”，才谈得上“不怕没柴烧”，否则上天入地贴墙上，脑袋掉了碗大的疤，可二十年后还是不是一条好汉，没人打得了包票。叫我来猜，不翻出能免死底牌，让人铤而走险没说服力――跟命悬一线的人谈，谁能开出比这更诱人的价码？！李庄拿“我代理的不会死”说山，龚爆李庄的“点”，估计条件，不可能低到哪儿去。<br />
&nbsp; &nbsp; 但能踩住高人垫脚，跟资深刑辩律师换位，这叫魔高一丈棋高几筹。找那真能拍板的，就赌一张不扯三局两胜的闲淡，要庄家够横要玩家够狠，要开弓没有回头箭。卷入牌局当当看客，加小心都可能成了筹码，非要下场试试手气，那是倒霉催的。在龚而言，若李庄“伎俩”得逞，证据或将被推翻排除，有望免死但未必笃定――“勾者十六七，留者十三四”，这回不是斩监候赶上秋审，他输不起。得知道印把子攥谁手里，知道李庄没抓着那张免死牌！就算侥幸免死，律师不光在功劳簿上喜孜孜续写新篇，“事成后”那两三千万的“鹰”，还是要撒出去的。若李庄行径败露，只需确保他败在自己出首，则免死目标依旧可能达成，鹰都能借机免撒。是以这局，龚或可大胜，律师则完败到一塌糊涂。律师卖起大力丸来，拍的大多是法官的后背，假如法官说了不算，委托人又当如何呢？<br />
&nbsp; &nbsp; 敲打律师让他们知道收敛，是为政者该有的预期。天下苦秦久矣，黑恶千夫所指，重庆吏治伸手不见五指，铁腕打黑如此令人雀跃，比“唱红”来劲也更让人陶醉。但如此的运动打黑，专案侦办、隐秘羁押、代号匿名、阻挠会见和阅卷，乃至乌小青蹊跷自杀，已令其正当性和效率倍受质疑。律师拿人钱财，找程序岔子哪壶不开提哪壶，不胜其烦清理之中。中青报过于及时的报道，大段直接引语除非出自面壁――小郑声称看了卷宗，说明李庄在看守所在夜总会与人沟通细节，早已被一览无余――看个卷律师都难，你就有这么大的面子？只可惜，为惩治无良律师不惜牺牲程序正义，甚至打闷棍下蒙汗药，想来韦小宝也不过如此！苦于监听所得难以端出，“反戈一击”才成了回春妙手。渝方这局，只算惨胜。<br />
&nbsp; &nbsp; 但龚对律师的揭发，使律师对当事人的信赖，突然间溃不成军，“这样子嗷！”打黑案中被告人，很难指望律师傻卖力气。好比前方出了交通事故，后来者每人一脚“点刹”，车速会立即降下来。律师“点刹”过后，终于看明白了，有无金刚钻儿，揽活儿都得掂量，你得知己知彼，知道能吃几碗干饭：这里“够黑”不假，“钱多”也靠谱儿，但“人傻”可未必，“速来”与否，那得一慢二看三通过。如临深渊如履薄冰，小心行得万里船，这样才稳当。如此则所有的辩护，都该是走走过场。<br />
