接着他们一个头问我今天准备干嘛，我说不是正在睡觉吗，他又问今天下午，端午节下午准备干嘛，谁组织的？我说不准备干嘛，不知道他说什么是谁组织的。他们又打，并拿我的衣服套住我的头，将我架下楼装进车内，然后拉到一个地方，进去后到一个办公室内（后来得知是北京市朝阳区麦子店派出所）。<br />
在折磨我一阵后，开始要我交待问题，说是刑事讯问，我说你们违反了程序法，我希望你们按程序来，他们就又拳打、用装满水的矿泉水瓶子殴打、用脚踢我的脸，我说我要主张我的公民权利、人身权利，小头目说“你在里面没有权利”。<br />
随后我要求跟律师通话，他们说不准，我说这样我就再不说一个字了。他们用各种方法想让我说话：侮辱、诬蔑、套近乎、开玩笑，甚至拿收缴我的《古兰经》来开玩笑，戴眼镜的小头目拿着《古兰经》打开翻了一阵，把我两腿并拢，打开《古兰经》后放在我腿上，拿我胳膊压着《古兰经》说，你就读你们的书吧，其中有两个小兵，三十岁左右，很胖，一个叫杜凯（音），一个叫小王，小王说，妈逼的你是拉登手下的吧，恐怖分子吧？但我都没说话。过了一段时间后，先前在我家问我准备去哪儿的大头目进来。撕掉没有我的回答内容的讯问笔录，带着很脏的话说，还想请律师，律师来了连他一块打跑，告一回打一回，一直打死。<br />
接着又开始殴打我，中间他们还吃了一顿盒饭。随后又套上我的头，拿凳子等东西砸我全身，致使我头疼头晕，可能有些轻微脑震荡。<br />
在殴打我时多次恐吓我，多次说要弄死我，说整我的方法多的是，那大头目还威胁我说：现在是我和你的私人恩怨，问你话你不理，我一定要整死你。并在殴打后说，再给你一次机会，你考虑一下。<br />
过了几分钟后，又过来说，想好没，我还是没说话，他就又殴打我并企图让我跪下，但没有得逞。然后他又问我说不说，我没理，他就说，好，你不说，就送你到不说的地方。<br />
随后他们拉上蒙着头的我，到外面上了车，一路开过高速公路、带急弯的盘山公路，经过一个多小时后，停了车，拉着我下车到一个地方，又按着我的肩膀踹我的膝窝喝令我跪下，我没有跪，他们就停了，因路上脖子被衣服缠得很紧，加上双手被绑在背后，我窒息得一直想吐，就坐在了地上，他们就踢我的腰和背。然后说，你很硬，我们很佩服你，但你要是再敢到北京，我就弄死你。说完就取下套我头的衣服，过了一会儿才解开绑我的皮带，把我的手机扔在前方的山林里，转身迅速上车开走了。当时时间大概在凌晨三点半左右。<br />
