																											<div id="postmessage_640126" class="t_msgfont">从“热衷八卦事业的兴趣小组”到1984BBS 的转变本身可见论坛定位的显著变化。<br />
&nbsp; &nbsp; 当然这是书记领导下的论坛领导的重大决策和重大部署，我坚决拥护站方的决定。：）<br />
&nbsp; &nbsp; 而今有罗马假日板块还带有八卦小组的遗风，并在一往无前戏（不是结扎的马甲）同学身上得到了体现。站方给与我们发表言论的自由和场所，书记等人也承担着更大的风险，我这个论坛混混从08年溜达到现在也受益颇多。：）<br />
&nbsp; &nbsp; 所以我觉得1984BBS的价值是有的，也因此向好友介绍进来，感谢抑扬大叔每次有求必应。<br />
&nbsp; &nbsp; 这个帖子的目的在于，论坛目前存在一股浮躁的气息（这几天蛋疼，跳出来法牢骚）。<br />
&nbsp; &nbsp; 在罗马假日发“大家每次嘿咻的时间”有人就喊没意义，不值得讨论。那个王朔骂袁腾飞的假新闻帖子，也是大骂王朔声一片。而野夫老师写王朔那个帖子则显出发贴人的智慧与“庸众”的不足。现在雅典成了讨伐党员的高潮，忘记了“人人都是体制的一部分”。是否过于严肃或者激烈？是否盲目追逐政治正确，而不思考？这些在贴子主题方面很显著的浮躁。<br />
&nbsp; &nbsp; 另一个浮躁的表现在于讨论的方式，动不动就问候人家女性亲属或者进行个人鉴定，这已经不单单是讨论这么简单了。这样不是就贴子中的论点或问题而争论，而是观点伤害了他们的感情，已经到了不论是非 只分立场（恩怨）的境界。希望热衷于讨论并发现其价值的卦友可以内省一番。温和一点未尝不好，妥协与沟通是交流的价值。回到辩论本身，说服对方为目标，则要懂得沟通的技巧。（再赞一下nkpoper 、妖道、阿文强等大叔温和的态度！）<br />
&nbsp; &nbsp;&nbsp;&nbsp;当然，论坛上面隐约可以看到的“论坛要求”，涉及人身攻击应受处罚，奖罚分明应是常态。尽管劣币驱逐良币是每个论坛常态，但论坛若想发展壮大就要考虑了。此外，每次大清洗总得清洗数千ID，这说明论坛话题和风格不对口味，但是能申请的大多是热衷自由人士，论坛虽然小众，切勿成为小团体，若要发展，则海纳百川应是正道。<br />
&nbsp; &nbsp;&nbsp;&nbsp;戒骄戒躁，切记成事之路还很长远，勇气虽然可嘉，策略依然重要。<br />
&nbsp; &nbsp;&nbsp;&nbsp;回到站方本身，就我在论坛和推特上观察，书记最近有些部分浮躁了。不管在论坛还是与花落去在推特上的讨论。（书记勿骂）我表态，我是衷心支持书记的！<br />
&nbsp; &nbsp; 人小言微，跳出来发发牢骚。若不恰当，请予我当头棒喝。<br />
&nbsp; &nbsp; 作为一枚老人（id是新注册而已），挺怀念“热衷八卦事业的兴趣小组”的。：）<br />
