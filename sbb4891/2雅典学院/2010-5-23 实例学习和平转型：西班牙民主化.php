<br />
　　老将军们对这个回答表示满意，认为苏亚雷兹敌我分明、立场坚定。于是，他们支持了他的政治改革方案。 <br />
　　苏亚雷兹启动改革最难解的一个死结终于解开：他有了将军们的承诺。事实上，改革进程一开始，共产党就取得了合法地位，开始参与竞选。所以，一些保守派至今认为，是苏亚雷兹欺骗了这些年迈的将军，耍了计谋。苏亚雷兹始终不承认欺骗。多年后，已经下野的苏亚雷兹接受访谈，坚称自己从来没有、也不可能欺骗“西班牙的军人”。他坚持说自己是诚实的。是共产党改变了纲领才合法化的。他确实要求共产党先改变自己，承诺遵守游戏规则，不再企图推翻政府，这是参与到民主游戏里来的“准入条件”。 <br />
<br />
　　拜会将军们两天之后，内阁讨论政治改革法。内阁里的四个军人阁员没有反对。几天后，内阁起草工会组织改革法案时，一位将军却表示强烈反对，他说，内战就是当年的工会组织胡作非为才打起来的，工会这一步不能放。苏亚雷兹认为，工会这一步非走不可。没有自由的工会组织，大众就没有参与政治的途径。僵持不下，苏亚雷兹以首相身份要求这位将军辞职。他周围的人担心，这一举动会引起军队反弹，导致政治改革法翻车。苏亚雷兹却根据自己对军队的了解，相信条件允许他在这一刻显示强硬。 <br />
<br />
　　这一冲突相当紧张，但是苏亚雷兹赢了。 <br />
<br />
　　佛朗哥去世不到一年，在他留下的政权体制内，西班牙的政治改革正式启动了。 <br />
