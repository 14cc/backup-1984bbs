													<h2>实例学习和平转型：西班牙民主化</h2>
						
						
																											<div id="postmessage_633905" class="t_msgfont">以下摘自维基百科：<br />
<br />
西班牙民主转型（西班牙语：Transición Espa&ntilde;ola）是指西班牙从佛朗西斯科・佛朗哥的独裁统治下转变成自由民主国家的一个时期。这一转型一般被认为从1975年11月20日之后弗朗哥去世时开始，而对于它的结束时间则有不同的观点：1978年西班牙宪法的颁布，1981年安东尼奥・特赫罗未遂政变的失败以及1982年工人社会党在大选中的胜利胡安・卡洛斯一世的政治角色。<br />
<br />
佛朗哥的去世将胡安・卡洛斯推向了王位。直到佛朗哥去世前，胡安・卡洛斯一直躲在幕后，而且看上去他追随着佛朗哥让他成为佛朗哥继承者和西班牙国王的计划。成为西班牙国王之后，胡安・卡洛斯帮助现有西班牙政治体制的发展，就像他的父亲，唐・胡安在1946年之后提倡的那样。<br />
<br />
民主转型是一项雄心勃勃的计划，这个计划被寄托在来自西班牙内外的支持上。以美国为首的西方国家开始支持西班牙的君主立宪制，许多西班牙和国际上的资本主义者也都支持这一体制。<br />
<br />
然而，由于内战（1936-1939）的幽灵仍在折磨着西班牙，民主转型被证明是充满挑战的。极右的佛朗哥主义者得到了西班牙军队相当大程度的支持；左翼民众则不相信一个得到佛朗哥支持的国王。<br />
<br />
民主计划的实现需要左翼抑制其最激进部分的挑衅，而有着佛朗哥主义元素的军队不能介入这一政治进程。<br />
<br />
刚开始统治时，胡安・卡洛斯没有离开佛朗哥的政治体制。他对民族运动（es:Falange Espa&ntilde;ola），佛朗哥时代唯一合法的政党的原则宣誓效忠；在佛朗哥主义者把持的西班牙议会前取得皇冠；尊重组织法对政府首脑的安排。他只在议会召开前发表的演讲中暗示了对西班牙政治转型的支持。<br />
<br />
<br />
<br />
君主政体下的第一届政府（1976年1月－1976年7月）<br />
<br />
胡安・卡洛斯一世的第一届政府由卡洛斯・阿里亚斯・纳瓦罗主持。国王按照1966年组织法的指示从一个由王国理事会提交的3人名单中挑选了阿里亚斯・纳瓦罗。<br />
<br />
对阿里亚斯・纳瓦罗的任命没有预示大规模的政治转型。不过，新政府中包括了3个曾经与佛朗哥政权合作但如今支持民主转型的成员：José María de Areilza，被任命为外交部长；Antonio Garrigues y Díaz Ca&ntilde;abate被任命为司法部长；Manuel Fraga Iribarne被任命为内政部长。但是，为了保持政治平衡，国防部副部长一职被无条件的授予亲佛朗哥的费尔南多・德・圣地亚哥将军。<br />
<br />
不久，另外两个官员在民主运动中突出出来。担任议会议长，属于民族运动的大学教授Torcuato Fernández Miranda。刚刚在民族运动中崭露头角的阿道夫・苏亚雷兹担任了民族运动秘书长一职。<br />
<br />
新政府面临着许多困难。在大量对赦免的呼吁下，局势的动荡在增加。同时，在巴斯克地区，紧张氛围也在持续增长。埃塔继续着它的袭击，许多巴斯克人支持埃塔的行动。1976年3月初在维多利亚进行的总罢工持续了数日。当街头抗议变得暴力时，警方的报复导致了3人死亡以及因铁腕政策而出名的弗拉加部长声望的损失。<br />
<br />
同时，反对派坚定的尽快要求肢解佛朗哥体制。反对派在1976年3月联合起来，组成了一个名叫民主协调的新组织。这一组织宣称它不会接受任何与佛朗哥秩序有联系的政治体制。阿里亚斯・纳瓦罗只想对佛朗哥体制进行小规模的改革，但一些部长已经预料到并且拥抱可能发生的更大变革。<br />
<br />
1976年6月，José María de Areilza陪同胡安・卡洛斯一世前往美国访问。他宣布完全支持在西班牙实行民主政治。阿道夫・苏亚雷兹与反对派中的温和派保持着频繁的联系，为民主转型的第一阶段做计划。胡安・卡洛斯一世本人在访问美国期间坦率的宣布他支持在西班牙重建民主。最终，在1976年7月，由于阿里亚斯・纳瓦罗依旧拒绝改革并反对民主化，胡安・卡洛斯一世要求他辞职。<br />
<br />
阿道夫・苏亚雷兹的第一届政府（1976年7月－1977年6月）<br />
<br />
议会主席Fernández Miranda将阿道夫・苏亚雷兹放进了政府首脑的3个候选人的提名名单中。国王选择苏亚雷兹是因为他认为苏亚雷兹能够面对说服由佛朗哥主义者组成的议会改革佛朗哥体制。这样改革可以在佛朗哥的合法体制下进行并且还可以避免军队对政治改革进程的介入。<br />
<br />
阿道夫・苏亚雷兹迅速做出了关于政治改革的两点决策：<br />
<br />
一旦政治改革法通过了议会表决和全民公决，在西班牙建立自由民主体制的制宪进程就将开始。<br />
要求在1977年6月进行大选选举出新的议会。这届议会将被委以制定民主宪法的重任。<br />
这一计划非常明确，但它的实现考验着苏亚雷兹的政治能力。他必须说服反对派加入这一计划，让军队不干涉这一政治进程，并且还要让巴斯克地区保持稳定。<br />
<br />
尽管面临着这些挑战，苏亚雷兹的计划还是在1976年7月到1977年6月间执行了下去。<br />
<br />
政治改革法<br />
<br />
政治改革法的文本由议会议长Torcuato Fernández-Miranda起草。1976年9月1日，苏亚雷兹政府批准了这一计划。11月，议会在Fernández-Miranda的主持下展开辩论讨论这一法案，最终以425票支持，59票反对，13票弃权的结果通过了这一法案。<br />
<br />
苏亚雷兹政府还试图通过全民公决来赋予政治改革方案更多的合法性。1976年12月15日进行了全民公决，这次全民公决的投票率打77.72%，94%的投票者表示支持政治改革。从这时期，选举进程（苏亚雷兹计划的第二部分）便可以开始。这一进程将选举出制宪会议的代表，这一制宪会议将负责起草一部民主的宪法。<br />
<br />
这一部分完成后，苏亚雷兹还有解决一个棘手的问题，他需要考虑他是否应将未参与政治转型的反对派纳入进改革进程中去，他要考虑是否应与反对佛朗哥主义的反对派接触。<br />
<br />
苏亚雷兹政府与反对派的关系<br />
<br />
苏亚雷兹采取了一系列慎重的措施来增加他的计划的可靠性。1976年7月，他宣布赦免400名政治犯。1977年3月他又宣布了一次赦免，进而在5月宣布大赦。1976年12月，佛朗哥时代的秘密警察Tribunal de Orden Público (TOP)被解散。1977年3月，罢工合法化，4月，结社自由化。新的选举法在1977年3月采用，新的选举法使得西班牙的选举系统与其他自由的议会民主制国家相同。<br />
<br />
通过这一系列的措施，苏亚雷兹满足了反对派组织在1974年提出的要求。这些反对派在1976年11月碰头以求建立一个民主主义组织的新平台。<br />
<br />
1976年8月，苏亚雷兹通过与工人社会党总书记费利佩・冈萨雷斯的见面开始了与反对接触的进程。社会党领导人积极给予了苏亚雷兹支持。另外一个阻碍西班牙政治正常化的问题是西班牙共产党（PCE）的合法化问题。西班牙共产党在所有反对派中最活跃，也最组织化。1976年9月，在一次苏亚雷兹与军方最重要领导人的会议中，军方明确表示反对西班牙共产党的合法化。<br />
