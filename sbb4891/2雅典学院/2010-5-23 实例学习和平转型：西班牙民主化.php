言行分寸往往决定效果。而把握分寸恰是苏亚雷兹长期在体制内训练出来的本能。他知道什么事情现在可以做，什么还不能。他取下了首相办公室里前任挂的巨幅佛朗哥像，引起一位老将军的狂怒，苏亚雷兹却丝毫不为所动。在大赦问题上，他当着国王的面和军中将领发生争执，坚决表示他作为首相，绝不容忍无理要求。但是一转身，他在背后对将军们妥协，将一些“埃塔”成员在大赦后秘密驱逐出境。 <br />
