													<h2>有产者治国？嗯，我差不多就是这个意思</h2>
						
						
																											<div id="postmessage_341342" class="t_msgfont">民主有其弊端，最大一个就是容易导致“多数人的暴政”。市场却不是一小撮经济学家说了算，而是千千万万市场中人互动的结果，经济学家只是主张让市场自己去调节，尽管市场本身也会出问题，但比政府主导出的问题要小得多，也容易解决。所以，如果多数问题交给市场去解决，要比政府解决好得多，无论这个政府是民主的还是独裁的。<br />
<br />
市场跟自由民主是有关系的，市场在保障人们各项权利上的作用，比起写在纸上的法律更有效。比如30年前言论自由也是写在宪法里的，但那时的现实中有言论自由吗？没有。30年后的今天，言论自由在现实中多少已经有了一些，这是为什么？因为市场，因为有利益的多元化主体。利益多元化其实根子在所有权多样。而所有权是什么？是生产关系！就拿网站来说，它不允许人们说一些真话，谁会来看？谁会给它投广告？那么现在又是什么情况呢？是呼吁政府干预的氛围中，市场又在萎缩。而市场空间的减少就意味着自由的减少。所以，探讨市场对保障自由的作用是很有必要的。 <br />
<br />
04年以来，中国已经露出走计划老路的苗头了，最近两三年更是在呼唤国家干预的舆论氛围中走得越来越远了。这种情况下还在呼吁干预，岂不是要把经济喉咙完全交给政府去扼吗？而抓住经济喉咙就等于扼住了其他喉咙！他们呼吁的结果远离他们的初衷却还看不出来。真是可怜又活该！ <br />
<br />
宪政，限制政府权力，法治，规制各方取利，都是市场中人利益博弈的结果。 <br />
<br />
为何有新古典经济学和新兴古典经济学（杨小凯）的兴起？就是要在一定程度上回归到民族国家兴起之前的状态，那时的市场才是真正意义上的市场，因为那时国家和政府的手伸得没那么长。比如“克伦威尔入侵前的苏格兰，有长达上千年的时间，没有国家管理的司法存在的痕迹。”（权力与市场，罗斯巴德，欧洲代议制政府的历史起源，基佐），那时的法院都是大学教授自发组成的，经费由私人赞助，这些法院相互竞争（看谁判得公正）。民族国家兴起后，国家和政府的权力急剧膨胀，没收了许多本该由私人掌控和运作的权力。由此才带来更多问题，比如通胀就是政府为伪造任期内的政绩（恰恰又因垄断了货币发行权）而故意制造的。 <br />
<br />
民主究竟是民做主还是精英做主？深入研究过民主制度的人都知道，所谓民主本质上还是精英统治，也就是一小撮人统治，卢梭意义上的主权在民完全是天方夜谭，因为，全民统治是根本不可行的，票选上去的人也不可能与投票人的思路完全一致，投票时与当政后变化也很大，票选的民主制度依然摆脱不了一小撮代替大多数思考的弊端。所以民主充其量也不过是最不坏的制度。但市场不一样，他不存在少数服从多数的问题，胖人可以买到宽大的衣服，小脚的可以买到小号的鞋，等等。那么，很多市场自身可以解决的问题，为何非要交给貌似可以代表民众其实无法代表的政府呢？市场论者的意思就在这里：把本来就可以通过市场很好地解决问题、却被政府没收的权力和事务再交还给市场。比如定价权等等。 <br />
<br />
关于司法独立，印度和拉美国家的司法都是独立的，但并未起到独立的司法在美国那样的作用。为什么？因为那里穷人多，没有形成相当数量的中产阶级，这些人经过票选是进不了议会和政府的。可见，真正能发挥实质作用的司法独立本身之成为可能，必须有赖于培养相当数量的中产阶级。而中产阶级只能在市场中诞生。 <br />
<br />
美国建国初期，中产阶级就是相对富有的人，那时的美国穷人本身就比拉美少，所以进入十三州议会的往往都是有产者（法律本身要求当选者必须有多少以上资产），也正是这些富人才制定了伟大的美国宪法（一部经济文件，查尔斯比尔德）。至于英国，人们当初所以喊出“无代表不纳税”的口号，恰恰是因为他们有财产作后盾，而财产是怎么来的？还不是市场？ <br />
<br />
为何英美100多年前就有了市场经济和中产阶级，而我们今天还没有？因为我们没有私有制，没有私产权，这都是生产关系。<br />
<br />
环保的问题依然是产权界定不清造成的，一旦界定清楚了，是可以通过市场解决的。土地一旦私有，污染就可解决。没人愿意污染自己的土地！ <br />
<br />
私有化必然伴随着法治，否则，你说那块地属于你，谁承认啊？此其一。二，所有权人污染自己的土地是他的权利，谁也无权干涉。三，但你别污染了别人的，地也好，地下水也好，空气也好，一旦污染了这些，你就要吃官司。 <br />
<br />
再回顾一下美国宪法是谁制定的，是55个有产者签署起草的！如果是身无分文的穷光蛋，美国历史就要重写了。可见，财产权是多么重要和优先！ <br />
<br />
假如你们是一家公司的股东，你们会允许股本最少的人进入董事会进而出任董事长吗？不会吧！为什么？因为没财产或少财产的人没负担或少负担，责任心也弱，容易乱来。当然，穷人也可以掌权，通过什么？革命，就像60年前那样。结果怎样？乱来，民不聊生！ <br />
<br />
霍尔姆斯大法官说：“法律的本质是习俗。”立法者不可能凭空坐在那里想出法律来，也不可能预见到将来如何规制。他们的任务是将习俗（行规）提炼升华，从而形成文本（成文法）。英国连文本都没有呢，他们采用的是习惯法或案例法。而制定法律的人必须是一些很有责任心的人。责任心何以保障？从英美宪政历史看，最可靠的首先是财产。当然，随着历史进步，依靠智慧等无形资产的人也越来越多地进入立法机关。但实际上这样的人也不可能穷到哪里去。 <br />
<br />
“知识就是力量”？哈哈，20多年前我的外号就叫“培根”，这个说法已经很陈旧过时了，这是人把自己看得太了不起的结果。也正因为这样，人们以为人的理性可以做到万无一失，才不自量力地坐在那里企图计划一切，结果是人间地狱。 <br />
<br />
“有产者治国”？嗯，不错，我就有这个意思，但不完全，还有其他意思：现有民主模式比起经由更多市场手段所能达到的民主还差的很远。换句话说，我不满现有民主依然是精英统治、是一小撮人统治的现状，如果把更多本应由市场解决且能解决得更好的事务再重新交给市场的话，这个世界会变得更加美好一些（当然要记住，这个美好依然是大致美好），因为市场不是一小撮人统治的，而是多数人自由主张、自食其力的，因而也是最公平的。 <br />
<br />
有人说自由竞争中形成的垄断（网友原话是“专业化机构”）消费者无法抗拒，只会任其宰割。我说，一个充分竞争的市场中，人们所担心的专业化机构自有其他专业化机构去抗衡，百姓从中受益，根本用不着政府出手。 <br />
<br />
经济危机是怎么爆发的？1，在少有或没有政府干预的情况下，市场中相当一些人同时犯错就会爆发，而“相当一些人同时犯错”的概率当然是小的；2，政府干预引发经济危机，多数危机恰恰是政府干预造成的。比如美国大萧条。3，即便同意“追求短期利益放弃长远利益，在市场中从来都不是小概率”，但与有任期限制的政府追求短期利益相比，哪个发生的概率更高，哪个更值得容忍？4，交换更意味着放弃和得来。比如安乐死，病人用放弃自己生命的办法换取痛苦的结束，这难道不能用市场来解释？ <br />
<br />
有人认为，“只要有足够的利益，大多数人会犯污染自己的土地这个错误。”请问，什么叫足够大的利益？跟污染土地相比，这个利益大概是找到了新的舒适的居住地，比如哪个星球？如果真是这样，大多数人污染土地这种错误还叫“错误”吗？只可惜，“大多数人会犯污染自己的土地”这个想法不成立，原因很简单，那就是经济学的另一个重要前提：资源的稀缺性。稀缺性决定了人们不可能那样傻。 <br />
<br />
讨论至此已经接近得出结论了，但还有关键一个问题可以提出来反驳我。我给了他们一下午机会思考提问。无奈，没人提出。好吧，那我就自问自答：<br />
<br />
问：既然你说政府是一小撮精英在决策，容易犯错，那么，市场中的企业决策何尝不是一小撮精英做出的？这又如何解释？<br />
<br />
答：没错，企业决策也是一小撮精英做出的，但企业决策没有强制性，消费者可以自由选择，企业不可能强迫你购买。而政府决策具有强制性，消费者无法抗拒，别无选择，由此失去自由。此其一。二，企业决策也会出错，但因直接与自身利益密切相关，改起来会非常迅捷，成本也由企业主自己承担。政府犯错改起来就慢多了，成本还要纳税人承担。想想水门事件中的尼克松吧，想想周老虎事件中的陕西政府吧，多顽固啊！三，企业决策其实才是真正的民主。比如盖茨，他很富很有权，但他的富有和权力从何而来？还不是满足了千千万万消费者的需求使他们自愿地自下而上投出货币选票得来的吗？这不是民主是什么？这样的民主不比现有的政治民主更民主？ <br />
<br />
最后说说垄断的问题。市场中自然形成的垄断（区别于中石油这样政府赋予的垄断权），人们只能忍受，如果政府出手干预，只会打击各种发明专利人的积极性，导致他们不愿在攻克疑难问题上投入更多，从整体和长远来看，弊大于利。想想爱迪生吧，他多自私啊，自私到发明得来的多数财富都用于打官司――控告那些侵权者。如果没有他追求和维护利益的动机，他能有那么多造福人类的发明吗？ <br />
<br />
何况，世界上没有什么东西可以永恒，类似雷曼兄弟这样的百年老店倒闭的还少吗？谁能确定明天一定不会有“宏硬”之类的打破微软的垄断呢（假如真是垄断的话）？将来的事谁能预言呢？想起一句名言送给大家：“预言的作用的是有害的。”（不好意思，前不久我刚预言了一次A股大跌，没被确定的那个时间段验证，似乎拖到了昨天才开始，哈哈）。<br />
