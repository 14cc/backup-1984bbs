													<h2>姜文的环形废墟――太阳照常升起</h2>
						
						
																											<div id="postmessage_20910" class="t_msgfont">说话但藏起说话者，<br />
征服却藏起征服者，<br />
去死却藏起死亡。<br />
――布莱希特 <br />
<br />
姜文的这部电影，刚刚播映，就被学院派批评者给予“跳来跳去”、“对旧时代的迷恋”、“不好好讲故事玩弄观众”等等反智主义式的定语。我不清楚这些学者们是如何去看这部电影的，更不知道他们是如何去品味阿伦.雷乃的电影《去年在马里昂巴德》的。就我看来，《太阳》作为一部被误读的票房奇差的影片，有以下三个原因：一是姜文高估了国内电影观众的中国当代史的历史素质。二是暴露了我们的一些学院派电影批评者批评功力与审美能力的浅薄。面对一部全新的充满隐喻与象征的影片，他们根本不具备任何解读能力。我们的很多学者，其本质是一头眼睛上蒙了布围绕着意识形态之磨旋转的驴子，他们的嗅觉决定他们只能闻嗅出政治、男权、女权等等意识形态的胡萝卜之味，一旦涉及非意识形态的领域，诸如电影艺术的隐喻、象征、时间、空间、美学、史学，便开始分不清东西南北。三是姜文本人的雄心太大。他想拍一部前无古人，后无来者的影片。过于膨胀的雄心使得姜文把一切美好元素皆放置在一部片子里，密集的隐喻，魔幻现实主义的诗意，并行的二元叙事（情爱叙事/政治叙事）导致影迷的消化不良与电影语义的自我混乱。这部影片需要有耐心且富有诗意的观影者，对于习惯于速食画面快餐的商业社会的观众，它无疑精致至不可理喻。事实上，这部影片是2007年中国影坛少数的好片之一，它的艺术价值远远在李安的《色戒》之上。<br />
<br />
鞋，女阴的象征<br />
<br />
鞋的隐喻在影片里出来三次。第一次出现在第一片断《梦》里。疯妈梦到铺满鲜花的铁轨上有一双鱼鞋，醒来后就去村里的小店买了一双。疯妈的第一次性生活，是在李东方之父的“我知道，我知道”的情爱话语中失去的。那只色彩斑斓的鹦鹉围绕着挂着鱼鞋的树枝呢喃着“我知道，我知道”之后，鞋子神奇失踪了，疯妈因此疯癫了。鞋子的失踪，是女阴被完全空置的象征。鞋子在这里不但隐喻着女性，更直接隐喻着女阴，导致疯妈疯癫不是失踪的鞋子，而是性压抑。为了强调鞋子与女阴的这一同一性，姜文在第二片段《恋》与第四片段《梦》里进行了反复的暗示。《恋》片段中，梁老师给林大夫拧衣服的时候，唐老师过来把一双皮鞋扔给了林大夫，林大夫奇异他如何把鞋子弄大了。唐老师的未婚妻在《梦》片段里向疯妈叙述：“他把皮鞋煮熟，套进鞋梆子里鞋就变大了。”“他说，他能把我的鞋弄大，也能把我的肚子搞大。”鞋子唯独在第三片段《枪》里缺失，但这个片段里小队长李东方与唐老师的妻子发生了性关系，鞋子与枪正面邂逅，隐喻毫无出现的必要。<br />
<br />
枪，阳具与权利的象征<br />
<br />
枪在电影的四个片段里，唯独在第一片段没有直接显现，而是通过疯妈的回忆录式的疯癫语言与手势来体现：“你爸的枪，最早这么长，接着是这么长，下来是这么长。”枪在这里一方面隐喻着李东方之父李不空（这个名字十分有趣，里面不空，权力不空）的政治地位的逐步提高，另方面隐喻着李不空的生殖器随着时间对疯妈的变更。第二个片段《恋》里，梁老师有一把闲置不用的猎枪给了唐老师，暗示了作为男性的梁老师的性无能。梁老师作为南洋归来的知识分子，却被国家主义深度阉割，成为一名丧失知识分子身份的食堂工人（身份阳痿）。在第四片段《梦》里，唐妻对疯妈说：“他把那个事说成打枪。”枪在整部影片里有两个隐喻，一个隐喻着男性阳具，另一个则隐喻着政权。第二片断的荒诞就在这里，令女人们疯狂迷恋的男人是一位丧失了男性权利与政治权利的双重阳痿症患者。这位阳痿患者唯一的嗜好就是偶尔摸摸女人的屁股。就这样一个被剥夺身份与自我枪柄根本无法做案的男人，却成了那个时代女人们疯狂迷恋的爱人与男人们疯狂驱赶的“流氓”。这种双重的混合身份是梁老师无法承受的生命之轻。这位被阉割的、丧失了肉体阳具与精神阳具，因为身份问题深度焦虑的知识分子，最终无法忍受现实的荒诞，含笑着用祖国母亲送予他的枪带自尽。在《美丽的梭罗河》的歌声里，飘泊了一生的精神流氓，终于在流质的水里，回归至他肉体与精神的共有家园。<br />
<br />
疯子与傻子<br />
<br />
众所周知，抗美援朝止于1953年。李东方却出生于1958年。这一年，是中苏关系恶化的一年。一本车尔尼雪夫斯基的《怎么办》的小说，暗示了李东方之父根本没有死，他不过是因中苏关系恶化而停滞在苏联。反复出现的台词“你就叫我阿辽沙吧，你就叫我阿辽沙吧。”隐喻着李不空就是苏联，疯妈就是祖国母亲。1958年，祖国母亲被苏联父亲欺骗。1976年，妈疯了，祖国母亲疯了。这位疯了的母亲却讲了个《树上的疯子》的故事：树下的傻子看着一口井数着“13，13……”树上的疯子跳了下，看井里有什么，傻子一脚将疯子踢进井里，数着“14，14……”电影里交待这个故事的作者是普希金。普希金没有写过这么个故事，倒是卡尔维诺写过一本《树上的伯爵》。但导演用普希金这一前俄罗斯诗人身份，很能说明导演本人的立场，风靡于20世纪的共产主义乌托邦，莫如说是在理性的傻子们的号召下对感性的疯子们所进行的一场激情大诱惑。<br />
<br />
生与死<br />
<br />
第一、二、三片段都以死亡做结，却配以狂欢之曲。长歌当哭，人生虚无。歌声、笑声、枪声中的死亡更令人哀恸蚀骨，但很多中国观众看不出这悲伤的底子，他们更习惯陪着银幕上的嚎啕大哭的主人公落泪。与影片里的主人公一同啜泣是中国观众的审美惯性。这是一部本质上虚无的片子。疯妈站在屋顶高声吟诵“昔人已乘黄鹤去，此地空余黄鹤楼”，莫如说是姜文本人的心声。第四片段《梦》是整部电影中唯一以出生做为主题的片段。而这生，我们都知道，等待它的是死亡，是十八年后的一声枪响。铁轨延伸，鲜花灿烂，沉迷于梦里的生存，亦不过是向死而生。宇宙照样旋转，太阳照常升起，它们不会因为人类的出生、死亡、狂热、悲喜发生任何的改变。<br />
<br />
天鹅绒肚皮与天鹅绒革命<br />
<br />
唐老师与唐妻是在疯妈失踪的那天到了南方。在第三片段《枪》里，唐妻代替了疯妈的身份，唐妻在此象征着祖国母亲。当唐妻与李东方媾和的时候，告诉李东方，“你唐叔说我的肚皮是天鹅绒。”当李东方问唐老师“天鹅绒是什么？”的时候，唐老师说“我可以告诉你，那他妈的就是一块布。”。唐为了告诉李东方天鹅绒是什么去北京寻找天鹅绒。他（崔健）的朋友指责了他，并在一张纸上画出唐与其妻、以及李东方彼此关系的指示图，指代唐的符码是一个五角星。在中国，人人知道五角星是军队、国家政权的象征。1989捷克的民主革命被人们称为“天鹅绒政变“、”天鹅绒革命“，因其不曾流一滴鲜血而获此荣称。而我们的1989，发生了什么？一些人知道些许，一些人根本不清楚那场席卷了一代人青春的残酷飚风。电影里，李东方说了一句”可是你的老婆的肚皮根本不像天鹅绒。”唐叔（执政者）转身就是一枪，随着一声枪响，李东方十八年的生命就此完结。他死于他说出了真相，他死于他知道，他的伟大祖国母亲的肚皮根本不是天鹅绒。<br />
<br />
直切主题的环形叙事结构 <br />
<br />
 影片是由疯（隐喻着乌托邦的幻灭）――恋（展现了幻灭的乌托邦里被扭曲的人性与情欲）――枪（隐喻着权利）――梦(隐喻着共产主义乌托邦)四个片断组成，很多人注意到梦才是真正的开头，却不明白这是姜文蓄意营造的闭合型环形叙事结构。姜文将梦放置在影片的最后，为的是表达这一永不休止的循环之圆，历史之圆，太阳照常升起之圆。<br />
<br />
以18年为一个循环的时间之钟&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; <br />
<br />
有人指责“这是一部缺乏任何当代感的作品”，显然不曾弄清楚姜文原本就不想赋予这部影片准确的时间感，他的雄心在于他想讲述一代人又一代人梦幻的破灭，以及一个古怪民族的循环式命运。只有在模糊的时代感下，时间的循环之圆才能从历史的巨大平面上凸显出来。这圆形废墟的时间之钟是以一个循环分为18个单位来标刻的。电影里明线的时间是在讲述1958至1976这18年间一代人梦想的幻灭，却将时间链向着过去延伸至1949年海外知识分子的回归、1952年的抗美援朝、1958年的中苏关系崩溃，向着未来则延伸到1989至2007这18年间。<br />
<br />
据我所知，姜文此片原本是计划在原定于在2007年5月的戛纳电影节举行全球首映，并于6月在中国和法国同步上映，但由于法国的后期制作公司的洗印事故，导致影片中多处对画面有特别要求的画面被毁，而错失戛纳，但该片入闱第64届威尼斯电影节竞赛单元，上映日期也推迟到11月。只要人们不曾健忘，大家亦会知道2007相对于1989的时间长度是多少。中国是个迷信18的民族。旧时菜市口的临死之人喊的最多的口号便“18年后，老子又是一条好汉。”我猜想，姜文拍摄《太阳照常升起》的本意无非是想给建国后的几个十八年铭写一篇最为漂亮的碑文。<br />
<br />
《太阳照常升起》不是姜文一个人的梦，它记录的是49后整个中华民族的瑰艳噩梦。为了通过广电总局的审核，姜文不得不模糊影片的时代感，不得不将影片切割成一堆万花筒里的艳丽碎片。本雅明在评论思妥耶夫斯基的《白痴》时说：“批评家和写作者大多惺惺相惜，因为水平一般的小说写作者讲的都是陈词滥调，而批评当然都能将这些陈词滥调叫上名来，正因为能叫上名来，就加以赞赏。这一范围正是批评所必须规避的。如果以这种概念来对陀思妥耶夫斯基的作品评头论足，就是无耻和错误之举。与此相反，应当把握的是陀思妥耶夫斯基的创造意念中，民族性及人道在形而上学意义上的同一性。”我认为，这段话同样适用于姜文的电影《太阳照样升起》。我们的一些电影批评家就很喜欢对他们能叫的上名来的影片大将赞赏。而对独创性的挑战他们大脑神经的艺术影片根本无有判断能力。这部影片需要的恰恰不是批评家的陈词滥调的式的肯定与否定，它更需要的是在把握导演的创造意念中，民族历史、民族性及人道在形而上学意义上的同一性。</div>

							
							
							
							
