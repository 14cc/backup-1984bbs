													<h2>我的革命宣言：我们的奋斗</h2>
						
						
																											<div id="postmessage_656686" class="t_msgfont">目前，主张武装革命的反共人士普遍处于一个尴尬的地位。反共群体中的所谓精英，那些可耻的民主派改良分子，妥协主义者和傲慢自大，自以为真理在握的狂徒――自由主义者；他们虽然分裂，内斗，做出让中共耻笑的狗咬狗勾当，然而，这些软弱的臆想症患者，狂妄的疯子们。却将我们――真正愿意面对现实的武装革命派当成他们眼中的“人民公敌”大加攻击侮辱。在这帮连四则运算都算不明白，把“劳动”给当成“资本”的所谓精英眼里，我们这些革命者是“村种”，“暴徒”，“流氓”。这些傲慢的精英，自己没有做成过一件真正有利于人民的事情，他们对社会的了解贫乏的可怜。以至于这帮蠢货们能把一张签了一万个名字的废纸当成圣经，公开的讲出那些与大众观念对立的言论――“薛兆丰：春运票价过低造成举国浪费 应充分提价”；多可笑！这话能公开说出来吗？<br />
这些蠢人，每天躲在书斋里对着《甘地传》，《自由秩序原理》意淫的“精英”。居然有脸攻击我们武装革命者，岂不是荒谬至极！<br />
很显然，在现时，我们革命的条件尚不具备。我们的革命言行自然而然的会被某些精英视作“口头革命”，“打口炮的意淫粪子”。但是，我们不需要在意，我们要认识到，所谓革命，绝不是革命家一手策划而来的。历史上的历次革命，几乎都是在社会两极极端分化，暴戾的气息和复仇主义思想广泛传播，经济长期萎靡不振的社会环境中，由于偶然的因素，爆发的群体性事件，最终造成的整个社会的大动乱这一前提下取得的成功。我们要明白，在革命爆发的时候，成功的革命者往往都还坐在小酒馆里面，面对突如其来的革命信息目瞪口呆。但是，胜利永远是握在有准备的人手中的。那些成功的革命者之所以成功，就是因为他们像我们一样，在几乎所有人都没有意识或者不愿意面对现实，承认革命将起的时候就开始准备相关的事宜，并在革命爆发后，以最快的速度投身并获得革命的领导权，最终取得的成功。<br />
因此，我们不用在意那些意淫的精英对我们“口头革命”的荒谬指责。我们不用生气。我们要牢记韩信胯下之辱的故事。要对我们的事业有信心！我们的成功，将会是揭露这些谎言的最好证据，我们的成功，将会使我们获得机会，将那些侮辱我们的，百无一用的“民主逗士”“自由精英”们，和共产党一起，送入地狱！<br />
我们要明白，比起那些可恶的精英，那些一边与大多数人为敌，一边喋喋不休”民主，自由“的蠢货们，我们具有更强大的优势！<br />
社会心理学家勒庞认为“科学为我们许诺的是真理，或至少是我们的智力能够把握的一些有关各种关系的知识，它从来没有为我们许诺过和平或幸福。它对我们的感情无动于衷，对我们的悲哀不闻不问。我们只能设法和科学生活在一起，因为没有任何力量，能够恢复被它摧毁的幻觉。” 。精英们是高高在上的，他们很难理解到底层民众脑海中在想什么。而我们能！精英们被他们学的知识所束缚――我从不认为知识的多少和是否愚蠢是有关联的，他们死板，刻薄，死抱教条不放。他们自以为是为了民众的利益鼓与呼，而他们的言行却是就将他们自己推向民众的对立面！<br />
但是我们不同，我们知道，理论归理论。现实是现实。我们不会说出”没有资本家，你们就只能跳楼“这样与大众心理为敌的言论。我们不会被书上死板的文字所绑架，去寄希望于没有群众基础，不被大多数人认可的自娱自乐，哦，对了，精英们给那玩意起了个很是自欺欺人的文字”非暴力不合作“。当然啦，当他们真跑到现实中来个“不合作”的时候，他们就会发现，来自共产党暴力机关的“暴力”是运动中必不可少的调味料。我们不会因为一件事情不符合所谓的“道德原则”就不去做，如果这件事情对完成我们的目标有好处的话。因为我们知道，政治斗争无对错，只有胜利和失败。只要坚持我们的目标――追随我们的偶像，皮诺切特先生。以铁血的手腕，建立一个全面自由市场经济体制的强权政治！那么，为了完成我们的目标，为了实现我们的信念。任何事情都是可以做的。只要这件事情符合我们的基本行动理念：在最小牺牲的前提下，最大限度的完成目标！事实上，何为对错，这帮自以为是的精英自己都没整明白，罗尔斯和诺齐克的支持者争论了这么多年，对正义是何物，有定论了吗？我们更不会因为所谓的“仁爱和宽容”，而去像敌人妥协，去宽容任何敢于与我们为敌的人。因为我们知道，都是“忘恩负义、心怀二志、弄虚作假、伪装好人、见死不救和利欲熏心的”（马基雅维利语），每一个人的潜意识中，都住着一个魔鬼，我们不会让敌人获得机会，唤出他们心中的魔鬼。我们更比精英们清楚，对敌人决不能给予一丝一毫的宽容，我们必须在取得胜利之后，以最快的速度对敌人予以毁灭性的打击，就连他们后代中的婴儿也不可以饶恕，要清楚，婴儿是可以长大的，而长大后的这些婴儿，会继续与我们为敌！<br />
那些可耻的精英们会指责我们是“马基雅维利主义者”。不错，我们就是马基雅维利主义者！历史上，胜利女神几乎每一次都站在我们的身边，那些些可耻的精英，又有什么呢！不要跟我提华盛顿，不要跟我提哈维尔。他们是真正的政治家，不是你们这帮自鸣得意的精英！华盛顿不会在大多数人都反对废除蓄奴制度的情况下废除奴隶制，哈维尔也不会无视主流民意，讲出“支持富士康”这样自寻死路的荒唐话！<br />
只有抛弃精英思想的我们，才有可能在动乱时期笑到最后！要知道，群体永远是缺乏理性的。任何自以为真理在握，试图用书本上的真理去影响大众的书呆子精英必然会失败。网络上遭遇无数口水和板砖的薛兆丰，张五常就是他们的榜样，我们的反面教材。我们要认识到，他们说的并没有错误。但是，他们与民意对抗，结果就是自己臭名远扬，所捍卫的“真理”也被愤怒的群众丢进了垃圾堆！<br />
是群众不需要真理吗？不是的，事实上如果他们换一种群众乐于接受的方式，问题不就解决了吗。很可惜，这帮荒谬的精英永远不会这么做，在他们眼里，自己是精英。理应领导大众，大众是愚蠢的，他们的思想是不配考虑的。<br />
哈，这些鄙视我们的狂徒，并不知道本可能握在他们手中的权杖，因为他们的愚蠢而失去，而落到了我们的手里。并不知道，他们自以为为大众着想，却已经不自觉的走向了大众的对立面。乌合之众组成的浪潮纵有千般不是，谁试图以一己之力与其对抗，最终必然会被浪潮吞没！我们必须明白，群体状态下的群众，尤其是在社会动荡时期的民众，他们是狂热，盲从非理性的。我们要认识到这一点，要学会运用这一点。　因为群体的这些特点，它象无头巨人一样，必然需要领袖，于是，“领袖们”就出场了，领袖其实都是不自觉地精通群体心理的专家，他们如何掌握住这股力量呢？不是就一个问题提出讨论，也不是辩解，而是“断言”，断言某某是反动派，断言某某十恶不赦，并且“重复”这些断言，于是，因为群体的易受暗示性，这些“断言”就可以被“传染”开去，将群体带入一种同仇敌忾的语境中。同时，我们还需要拥有强悍的，符合人们心中想法的，通过“断言”、“重复”、“传染”的演讲与宣传方式来宣传的信念。我们要明白，多数人永远是短视的。而看到了这一点就沾沾自喜，自以为超越大众的精英们却忽略了一个最基本的问题――长期看，所有人都是要死的！这些精英们不自觉的敌视大众，鼓吹精英治国思想。这是大众所不能允许的。因为每一个人心中或多或少都拥有贪恋权势的意识，每一个人心中有都有软弱的一面，表现在中国人现有的民族性中，就是他们内心中潜藏的领袖至上，崇拜强权的意识。所以，当薛兆丰式的所谓精英自觉不自觉的在与人民的主流思想为敌的时候，记住马基雅维利的教诲吧，革命者“可以和贵族为敌，但不能与人民为敌！”<br />
我很乐意的告诉各位武装革命论者。我们的时代就要来临了。目前的中国，经济持续恶化，贫富分化现象严重，底层民众越来越对执政者失去耐心。恶性事件层出不穷。暴戾气息四处弥漫。这是什么？这就是革命爆发的必要条件！我们应该认识到，共产党政治体制的稳固害了它们，使他们内部的体制性改革成为了“不可能的任务”。国际经济形势同样严峻，指望外援也是不切实际的。至于那些同样不希望革命爆发的“民主妥协派’，”自由主义派‘精英们，我们要有信心。他们没有能力在革命爆发前改变这个社会，从他们的老祖宗胡适到现在张牙舞爪的刘晓波，余杰，杨恒均。这些人叫嚣着“开启民智”一百年了，这一百年里他们又开启了什么？至于以铅笔社为代表的自由派精英，他们的那些与主流民意的观点，已经使他们永远的失去了舆论权和试图改变社会的最后一点可能！<br />
那么我们应该干什么？告诉你们，我们什么都不用干。在社会大动乱之前，任何试图抢先一步发动革命的人都是愚蠢的。我们不是改良派，试图通过不切实际的“群众非暴力运动”来改善社会环境不是我们的任务。恰恰相反，我们应该想想办法，让这些蠢人出丑，让大家看看这帮所谓的精英，是如何丢人现眼，炮制一幕幕闹剧的。这样一来，大众才不会被这些蠢人所蛊惑，白白的浪费时间，金钱，去自讨没趣。我们要记住，在机会到来之前，什么都不要做，就不会犯错。六四的前车之鉴，我们可不能忘记啊。<br />
当然，什么都不做，也不意味着我们一天到晚无所事事浪费时间。我们应该努力提升自己的相关素质，为社会大动乱的那一天做好应有的准备。不要理会愚蠢的精英们的嘲笑，不要在意自由主义派的鄙夷。我们要在坚定信念的前提下，努力学习知识，努力提高身体素质，让自己变得更加强大。我们应该认识到，社会动乱的过程中，弱者会被淘汰，站错队伍的所谓精英更会被淘汰！我个人向大家推荐三本书，《乌合之众》，《君王论》，和《战争论》。只要我们每一位革命者都读通了这三本书，那么，天下就必然会掌握在我们的手里！我们更要走进群众，贴近底层，切实了解他们――未来我们的潜在支持者的心中所想，鼓动他们的仇恨，激发他们的勇气。不要嫌他们肮脏，不要误以为他们愚昧。我们的敌人，民主派的代表杨恒均都不得不哀叹，承认推动社会大变革的决定因素就是那些被精英蔑视的所谓“愚昧，无知”的，以农民工群体为代表的底层草根民众“所谓乌合之众也“。那么，我们还顾虑什么呢？<br />
作为一个撒旦主义者，我本人并不信仰上帝。但我还是忍不住为我们每一个革命者祈祷，祈祷我们的安全，祈祷我们的信念不被动摇，祈祷我们的作为会取得成功，祈祷我们的决心会获得收获，祈祷我们会获得最终的胜利。更是祈祷我们，将带领中国走向一个真正的新时代！<br />
中国万岁！武装革命论者万岁！中国共产党，以刘晓波，杨恒均为代表的民主派精英，以饱醉豚，“铅笔经济研究生”为代表的自由派精英，一切我们的反对者，万碎，万碎，万万碎！</div>

							
							
							
							
