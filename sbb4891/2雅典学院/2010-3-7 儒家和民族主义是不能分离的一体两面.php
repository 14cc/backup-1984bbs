													<h2>儒家和民族主义是不能分离的一体两面</h2>
						
						
																											<div id="postmessage_506623" class="t_msgfont">民族主义和儒家是对立的吗？没有民族主义才是真正开放宽容的儒家吗？我们对这些问题的回答是完全否定的，认为没有民族主义才是儒家所谓开放包容的人完全是被理想主义和思想上的政治正确给框死了。主张儒家不应该与民族主义相结合的这些人十分幼稚可笑，使我想起了那个迂腐的宋襄公。以为自己放弃了民族主义，其他人也会放弃民族主义，这样世界就没有了民族博弈的战场。有这种想法是幼稚而可笑的，有这种做法的人也如宋襄公一样最终会祸国殃民身死而人笑。<br />
　　<br />
　　试问，儒家的本位主义（具体讲是儒家公羊学）是什么？――这自然是“夷夏之辩”。所谓“夷夏之辩”，就是要辩明“夷”与“夏”的不同。夷在古代是指周边少数民族，即四夷(东夷、西戎、北狄、南蛮)；夏是指诸夏，即中国。在当代，四夷就是指中华民族中的那55个少数民族；而夏则演变为汉族。如果没有了“夷夏之辩”精神，那么儒家就失去其体，没有根本了！可以说，“夷夏之辩”在古代就有民族主义精神的传承在里面，没有民族主义，何来亚圣那句名言：“吾闻用夏变夷者，未闻变于夷者”、“以华（改）变夷”呢？<br />
　　<br />
　　有些家伙居然说：“‘元、清’作为中国历史朝代，没人否认其存在性。可汉族皇权旁落，照样是事实。问题出来了，现如今，当你为元朝的广袤国土骄傲时，有没有被人指责为乐奴？当你在欣赏清剧时，有没有被人调侃成乐婢？”<br />
　　<br />
　　这是一个多么可笑而又无知的问题――试问，谁在为蒙元江山的广袤而骄傲？窃以为有两种人：<br />
　　<br />
　　1.某些文化界的“汉奸人士”――在他们眼里，缔造历史的总是那些强权者，在他们眼里只有对强权的畏服和肉麻的吹捧，其历史观根本就不是以是非与否正义与否为评判标准的，“成王败寇”的帝王史观才是他们所推崇的，在他们眼里历史是由那些少数暴力强权者（如成吉思汗）所缔造的，人民不过是这些帝王驱使奴役的工具，这些学者根本上就不是站在人民群众的立场去评价历史的。这些“汉奸人士”不过就是一些没有气节的知识分子，有才而无德而已，譬如周作人之流――可以说，行尸走肉就是对这些人最好的评价。<br />
　　<br />
　　2.广大被汉奸当局和所谓“汉奸学者”暂时蒙蔽了的群众，由于政治话语权和历史观的解释权被这些“汉奸精英”把持，这就使人民注定要在一段时间内被其蒙蔽。但是历史潮流也表明：这些“汉奸”不可能永远蒙蔽广大人民群众，清醒的人民群众迟早会把他们抛入垃圾之中……<br />
　　<br />
　　窃以为：当前百姓中不少人为蒙元和满清殖民中国时的异族历史而认同，被影视文化中的“辫子戏”所误导，恰恰是在当前不正当的历史观和汉族虚无主义的错误引导下造成的。<br />
　　<br />
　　有些人说蒙古也是中华民族的一员，蒙古统治中国与汉族没什么不同，今天不少汉人不也为蒙元时期疆域的广大而自豪吗？――我要说的是，这些人恰恰在用今天的中华民族去诠释历史上的中国，南宋时期抗击蒙古入侵时期，蒙古帝国是中国的一部分吗？那个时候有所谓“中华民族”一词吗？而且，南宋时期就是中国的“亡国史”，所以民间才有“崖山之后已无中国”的说法。有幸的是，朱元璋的明朝终于恢复了中华，恢复了汉家正朔的地位，在明朝看来蒙古人和色目人就是殖民者和二鬼子，他们许多人统统被贬为“堕民”。在有明一代的士大夫中在评价蒙元时，有为蒙元的开疆拓土而高兴的吗？蒙古帝国疆域与我中华子民有何关系呢？？正是因为明朝继承了儒家的正统思想，没有抛弃“夷夏之辩”，所以有明一代始终把蒙元当作恶毒的侵略者――否则，朱元璋“恢复中华”又从何谈起！？莫不成，今天的印度也要为当初英国殖民统治时期高兴吗？――那个时候人家英国可是“日不落帝国”哟！呵呵，这么说来印度也是“日不落帝国”喽！？<br />
　　<br />
　　另一个中华民族的死敌――满清在殖民中国的时候，惧怕汉人恢复中华，他们怕汉才家才是中华正朔的本色之说，即儒家“夷夏之辩”理论。所以满清大兴“文字狱”，满清特别是康熙、雍正、乾隆尤其痛恨“胡”、“夷”这些词汇，并明令禁止使用。雍正甚至专门在《大义觉迷录》里驳斥传统儒家的“夷夏之辩”，正是满清对汉文化的歪曲，对儒家思想的割裂和曲解，造成遗患后世无穷的灾难。满清作为异族统治者深知光靠满人无力统治中华，只有大肆培养“汉奸文化”，造就大批汉奸做满清的走狗，才能彻底改变“胡人无百年国运”的谶语。<br />
　　<br />
　　有清一代，以“文字狱”迫害知识分子，以高官厚禄拉拢腐蚀意志不坚定没有气节的知识分子，大肆培养“汉奸文化”氛围就是终清一代的永恒政策。<br />
　　<br />
　　没有了“夷夏之辩”，儒家就不成其为一家了。“夷夏之辩”是儒家的民族主义，它是保证华夏民族不为夷人同化的保证。<br />
　　<br />
　　一个有趣的现象：热爱儒家文化的民族――比如朝鲜人和日本人，他们也得到不少儒家的精髓，但是他们放弃了民族主义吗？恰恰相反，这些国家的“民粹”主义之强连当今儒家的发源地华夏都自叹弗如的。<br />
　　<br />
　　注意，在满清时期，儒家被满清篡改以后，变的没有“夷夏之辩”了。反倒是在日本“夷夏之辩”却被大树特树――很简单，满洲人是中国的殖民者，他们痛恨儒家；而日本看到汉人失江山后，则开始以“中国”自居，自命“中朝”。日本把“夷夏之辩”在演进为后来“明治维新”时期的“尊王攘夷”运动。<br />
　　<br />
　　没有了民族主义，儒家思想就是个空壳子而已，阉割了民族主义情怀，儒家对国民性格会造成巨大的不同。以满清为例，大肆阉割背离传统“夷夏之辩”，其后果就是满清国民唯唯诺诺，奴才性格，国民麻木，整个社会死气沉沉。而恰恰相反，看到中国亡于满清后，日本反倒以中国自居起来，其民族主义被迅速激发起来。可以说，民族主义使儒家在日本焕发了新生的生命力。<br />
　　<br />
　　日本名儒佐藤信渊1823年在其著作《宇内混同论》里就把传统儒家中的民族主义极大地激发出来，他就指出扩展疆域乃日本霸业的必然，必须先取朝鲜，再取满洲，则支那可图也。在他的论著里，满清殖民下的中国成为了“支那”，与僭越“中国”国号的日本比较起来，我们反倒成为了群丑。佐藤信渊反复强调“支那”与朝鲜、西域、暹罗（泰国）和印度一样，都是“衣冠诡异之徒”！日本只有发扬儒家倡导的“鞭挞四夷”，才能使这些“衣冠诡异之徒”慕德思威，稽首匍匐，拜我天朝。从而使我“中华”（即日本）名播四方……<br />
　　<br />
　　日本的崛起少不了民族主义――而民族主义不是无源之水，无本之木，它少不了以儒学为载体。没有了儒学的深厚文化底蕴，民族主义不过是个浮萍而已……<br />
　　<br />
　　我们注意到，自德川幕府时期以来，儒学在日本迅速兴起并与民族主义相辅相成。日本出了一大批儒家民族主义者。山鹿素行在其《中朝纪事》和《武家纪事》里，就倡导“日本主义”，日本为“中央之国”，是“中朝”。<br />
　　<br />
　　1716年，有人向幕府将军进献《辟疆录》时就明确在书中提到：“殷汤王令氐羌之夷从服，高宗入鬼方之远方夷地，大王征服混夷。孔子亦谓入九夷之地而开其国。此皆为大圣王，大圣人也。然，开拓夷地，不夸为功……”――在这里，倭奴居然振振有辞地引用儒家的经典甚至孔子的言论为其侵略扩张寻找理论支持。<br />
　　<br />
　　民族主义的儒家发展到18世纪70年代达到高潮，江户幕府时期的“国学大成者”本居宣长把日本的民族主义推前了一大步，其表现就是公然攻击中国，抨击中国，强调日本正朔论和日本中心论。在他看来满清不是中国，而日本才是真正的“中朝”、“中国”，所以他引经据典地以儒家理论抨击满清为“戎狄国”，并引用《春秋左传》中“戎狄无亲而贪，不如伐之”为据，主张挞伐已轮沦为“戎狄国”的“支那”。<br />
　　<br />
　　我们注意到近代史上日本的崛起恰恰是少不了民族主义的激发，而儒家对唤起日本的民族主义，并焕发日本民族的精神有极大作用。<br />
　　<br />
　　从林子平、本多利明、佐藤信渊再到吉田松阴，这些名儒莫不是典型的民族主义者，他们为后来日本的新政――明治维新，奠定了思想基础。<br />
　　<br />
　　佐藤信渊的思想极大地影响到了“明治维新”时期的日本三杰之一大久保利通，吉田松阴的理论直接影响了倒幕维新及明治政府的重要人物如高杉晋作、木户孝允、伊藤博文、井上馨、山县有朋等，可以说，他们都是大儒吉田松阴的门人。<br />
　　<br />
　　儒家对激化日本的民族主义病使日本走上资本主义道路可以说是起到了巨大的奠基作用。从儒家帮助日本摆脱西方殖民危机使其迅速走上资本主义道路的历程来看，儒学与民族主义使不可分离的。你很难想象没有民族主义的儒家会使日本迅速复兴，摆脱民族危机。<br />
　　<br />
　　由日本的例子，我们也要认识到中国千万不能在挺儒的时候废弃民族主义――主张废弃民族主义的都是包藏祸心的人，满清也挺儒，不过人家是废弃“夷夏大防”思想，废弃了中华的民族主义，“防汉制夷”才是满清的国策――尤其是防汉！<br />
　　<br />
　　所以，我们要回归真正儒家的本源，我们就是要提倡民族主义，要强调“夷夏之辩”！</div>

							
							
							
							
