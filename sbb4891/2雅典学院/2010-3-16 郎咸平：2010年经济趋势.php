主持人：刚才有一个人说，您在07年也来南京谈到房价的 问题，今天又来到南京，南京房价比07年翻了一辈，房价如果高烧的话，要不要退烧？这里有个数据，今年一月份南京二手房成交量大跌，这个泡沫何时会破裂？ 面对2010这样复杂的一年，老百姓资产如何增值保值，老百姓手上有闲钱怎么办？<br />
<br />
郎咸平：还增值、保值呢，今天是很差的时间，问我这样的问题。<br />
<br />
在08年年底、09年年初的时候，09年3月份之后我会跟你讲买黄金。可是到了2010年的2月这就是很差的时间，这就是选择，第一个银行存款，第二个买黄金，第三个地产，第四个股票。现在这四个选择都很糟糕。<br />
<br />
炒黄金吗？真正炒黄金的不是你，真正拉动金价的是华尔街，根据我的消息来源，华尔街的黄金目前正处于高位套现的阶段，12月中开始会高位套现，所以现在也不是买黄金的好时机。<br />
<br />
房地产就是因为他发烧的，下一步是什么呢？<br />
<br />
就是今年1月份的现象，股价今年有没有跌破两千不知道，所以这个选择也是很差的，因此在2月3号的今天，也怪你们主办单位了，今天把我请来是很糟糕的，就是2月份请我没答案，因为我水平不够，因为你就给我四个选择，我选不了，没办法，我水平不够。<br />
<br />
主持人：有人感兴趣一个问题，像郎先生您自己是怎么理财的？<br />
<br />
郎咸平：我跟你们水平也差不多，我就是搞外币对冲、房地产。<br />
<br />
主持人：郎教授您讲的如此危言耸听，是否验证了经济全球化是个陷阱？<br />
<br />
郎咸平：当然了，各位想个问题，为什么全球化？<br />
<br />
如果你是有话语权的国家，你是不是想透过全球化图利自己，你总不会透过全球化图利别人。他为什么全球化？肯定是利己不利人，这就是为什么全球化本身事后看来就是个国际阴谋。<br />
<br />
主持人：有人问大学生就业困难的原因是什么，怎样解决？<br />
<br />
郎咸平：中国是制造业大国，根据全球化观念，各位记不记得国际分工？<br />
<br />
什么叫国际分工？美国在30年前把产业链一分为二，制造放在中国，其他环节放在美国，包括产 品设计、原料采购、批发零售等等放在美国。美国大学生比例为什么这么高？因为真正需要大学生的不是制造，是制造以外的环节，而美国控制这些环节，因此他需 要大学生，制造需不需要大学生？不需要，所以很多工厂老板，从董事长开始到保安说不定一个大学生都没有。<br />
<br />
主持人：有人问会不会发生类似于美国这样的金融危机？<br />
<br />
郎咸平：我希望不会吧，因为这些都是连锁反应。比如说房 价大跌、地价大跌就会造成地方政府的财政困难。我把每个病的来源都告诉你了，这两个病没有解决好，六大药方没解决好，造成今天这种现状，我已经把所有问题 都告诉你了，你想解决问题吗？就按照我演讲的这些细节，如何解决第一个病、第二个病，我已经给了药方了，就看你做不做的问题。<br />
<br />
主持人：你怎么看待海南近期房价快速上涨？<br />
<br />
郎咸平：建海南旅游岛是需要的，问题是为什么拉斯维加斯的成功不能够复制到迪拜？<br />
<br />
请各位想一想，一个原因就是这种观光、旅游区能否成功和地产开发的比例成反比。地产开发越 多，它越不容易成功，地产开发越少越成功。你们去拉斯维加斯干什么的？通常去赌钱，去赌场周边玩的，但是你有没有在周边看到豪华别墅、度假商品屋？没有， 为什么没有看到？美国政府不允许，美国政府不允许随意去拉抬房价，你需要完全在美国政府严格监管之下，大量资金只能投入到专业旅游产业。<br />
<br />
迪拜呢？迪拜为什么会出现危机呢？因为迪拜搞了房地产开发，但是迪拜的房地产并不贵，最贵的也不过三万元人民币而已。就这三万元一平米人民币地产就能够打击到迪拜的旅游事业，你还没有警惕吗？<br />
<br />
我们一定要了解这是一个非常复杂的2010年，你想成功吗？那不是靠勇气的，靠你多学习别的国家、地区的经验，我觉得拉斯维加斯应该是我们学习的榜样。多学习经验，少走点弯路，会对我们国家更好。<br />
<br />
主持人：谢谢郎教授，下面他是想跟您探讨一个问题，全国 各地都在不同程度的进行城市化的运作，从政府决策部门到经济学家普遍认为在未来可以预见的十到二十年内，中国城市化水平就提升到50%以上。如果中国发展 成为高度城市化国家，农民变成工人、居民，那么谁去种地？剩下五亿人或者十亿人去种地，能否养活剩下的十亿人？他人认为只要一定城市化就可以，也杜绝城市 病的蔓延，不知道郎教授是否同意这个观点？<br />
<br />
郎咸平：我一向对城市化持保留意见，你不要以为农民进城就会富裕。我当然希望农民富裕，但是前提他必须得有工作，而且必须有收入非常好的工作才能富裕，我不知道我们哪来的逻辑，城市化之后就能富裕，这是表面现象。<br />
<br />
我们希望农民进城必须有个条件：他能找到工作，能够找到一个工资高的工作。那我问你，这个工作谁给的？是企业家做了好之后才能聘用农民，因此表面现象叫城市化，一个城市化能否成功要取决于这个企业能否有利可图。<br />
<br />
只有一个盈利的企业才有可能促进城市化，如果企业家都不赚钱，你不需要工人，那请问那么多农民进城之后他要做什么？要把握本质，如何为企业家解决、改善投资环境，改善产能过剩，让企业能够成长、壮大，赚更多的钱，他才能聘用农民，农民才有工作，他才能安居乐业。<br />
<br />
因此城市化必须有企业的利润才能推动，而不是强迫的城市化，必须是经过时间的推移，使企业慢慢赚了钱之后形成自然而然的像西方一样的城市化运动，而不是拔苗助长。<br />
<br />
主持人：接下来这位是来自江苏鑫信投资担保公司的，也是今天的联合主办方，他们公司主要服务于各类中小企业，选择合适的产业是他们非常重视的，请问在当前经济过热的情况下，哪些中小行业更容易生存发展下去？<br />
<br />
郎咸平：如果你要我预测每个人的行为方式，我不太好预测，如果中国政府产业政策我不太清楚，他东变西变的，我无法预测。<br />
<br />
主持人：请问您对于2月份只用一天时间就完成了当月放贷额度您怎么看？<br />
<br />
郎咸平：这不是银行本身的问题，其实很多媒体问过，你觉得我们银行应该怎么做？银行是被动接受者，只要这个社会企业都非常健康都赚钱，银行就会非常好，只要这个社会企业都不箫条不赚钱，银行就会不好。<br />
<br />
因此要取决于整个社会好不好，好的社会才能孕育出好的银行。所以我今天跟你讲，银行应该怎么做本身我们就不说，我们要把银行外的营商环境搞好，银行本身是很被动的。<br />
<br />
主持人：这也是一个问题探讨，今年CPI攀升，通胀预期已经不可避免，作为普通市民怎样在通胀大背景下资产的增值保值？一般人应该怎么提升自己的投资本领？<br />
<br />
郎咸平：这不是你我能够做的事，这个话不是我说的，我只能说对于我们小老百姓而言，通货膨胀是很无奈的事。<br />
<br />
买房子可以保值，但是买房子在其他国家和地区可以保值，但是在日本就未必。是房地产在中国能 不能保值这是未来才会发现的事情，我们现在并不知道。黄金等等是不能对抗通货膨胀的，股票市场在美国是可以的。问题我只能回答到这儿，因为这是很大的风 险，我简单讲的话，你说必然能够对抗金融环境的工具在中国不存在，那只能冒点风险。我不想误导你们该怎么做，我只能说我知道的事。<br />
<br />
主持人：最后一个是比较微观的问题，是担保公司的问题，担保公司作为金融的重要组成部分，但是大部分担保公司都在夹缝中生存，担保公司怎么样创造才能提高竞争力？<br />
<br />
郎咸平：香港的例子可以借鉴，担保通过社区关系网会使得 很多担保公司、小型公司有生存机会。不是在银行夹缝中生存，而是银行的特殊形态，必须是稳健的客户，安全质量必须先考虑，在这个前提之下，像香港等地有很 多小型的金融公司会出来，包括贷款公司，他就专门找银行客户。在浙江温州这种公司做得不错，而这个事情是银行做不到的，这不是夹缝中求生存，而是他有他的 市场。<br />
<br />
主持人：也就是说对于中国的这种新兴的民营机构您是赞成的？<br />
<br />
郎咸平：对，这是可以肯定的，这是有市场的。<br />
