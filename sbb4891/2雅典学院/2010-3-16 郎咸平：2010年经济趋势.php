																											<div id="postmessage_518850" class="t_msgfont">郎咸平：2010年经济趋势<br />
<br />
2010年2月 南京<br />
<br />
今天和各位谈一谈2010年的展望，不过我先念几个新闻给你们听。<br />
<br />
2009年8月26号，温家宝主持国务院常务会议提出，钢铁、风电等等行业产能过剩。今年一 开春，1月20号，又提出十大行业可能还是产能过剩，包括电力、煤炭、焦炭、有色金属、建材、轻工、纺织等十大行业。在我们想象中的经济回暖的过程当中， 怎么出现这种结果呢？请各位来宾想一想。<br />
<br />
截至到今天中午为止的消息，整个1月份北京房产交易量下跌70%几，上海下跌50%几，平均价格下跌 3%～4%左右。当然我必须承认1月份一向是淡季，但是跌幅未免过于巨大。这是什么意思呢？<br />
<br />
在整个去年回暖的过程当中，我们认识到几个有趣的事实，那就是中国的股市在2008年11月 份开始就已经连续四个月回暖，中国楼市在整个去年一年当中在全世界极度萧条的情况之下，是全世界唯一的逆市增长的行业，当然到1月份开始回调。汽车销售 呢？全世界各国产量大幅下跌，我们增长46%以上，此外还有奢侈品等等。请各位回忆一下，我们这一年的回暖喜悦当中，是不是真的像我两年之前所说的碰到了 问题呢？<br />
<br />
其实我以前就说过，如果全世界都没有回暖，而只有我们回暖的话，那就代表我们是不正常的。如果是不正常的话，这一切都要调整回来，也就是说为什么我们会回暖呢？<br />
<br />
不是真正意义上的回暖，而是我们发烧了。为什么会发烧？因为我们生病了。<br />
<br />
我想在今天三个小时的演讲当中，我把整个观念浓缩成一个小故事和各位做一个沟通。<br />
<br />
这个小故事是这么讲的：有一位A先生他挖金矿赚了100块，他可以有两个选择.第一个选择： 他给他10个矿工每个人1块钱，10×1＝10块，他还剩下90块揣在口袋里，因此他很富裕，这1块钱是什么意思呢？这1块钱仅够温饱之用。第二个选择是 给每一个矿工5块钱，5×10＝50块，他只剩下50块揣在口袋里。<br />
<br />
但是一旦做了第二个选择，矿工花了1块钱仅够温饱之外，他还剩下四块钱，而这个4块钱他又会拿出其中1块钱去买奢侈品，例如皮包、运动服、运动鞋……A先生突然发现，他多了一个投资的机会，他可以开一个大卖场，专门卖这些东西，比如皮包等等。<br />
<br />
为了卖这些产品，他还得聘10个女性的售货员，这10个男矿工和10个女售货员会结婚、生孩 子。然后他们就需要购买婴儿用品，又要花1块钱买婴儿用品。A先生这个时候发现，他多了第二个投资的机会，那就是生产婴儿用品。矿工夫妇已经花了3块钱了 还剩下2块钱了，他们准备住房子了，A先生突然发现他有第三个投资机会，那就是做地产开发，一栋房子多少钱呢？2块钱！等到A先生金矿挖完了，自然形成了 一个小市镇，自然而然的成长了。<br />
<br />
<br />
第二个选择我们给他一个名词：藏富于民。<br />
<br />
但是第一个选择呢？<br />
<br />
10个矿工每个人只拿到1块钱，那我问你，金矿挖完以后怎么办？10个矿工立刻失业。造成严重的社会问题，A先生不得已又从口袋里再拿出40块钱来，建条高速公路。然后这10个矿工又就业了，当建筑工人，那么高速公路一旦建完以后怎么办？10个矿工又失业了。<br />
<br />
这个时候A先生突然发现，当他做了第一个选择之后，他立刻面临两个困境：第一个困境他既无法 开大卖场，也无法生产婴儿用品，更做不了地产开发商，因为老百姓太贫穷。所以他的第一个困境就叫做：投资经营环境全面恶化。但是他又面临第二个困境，请你 想一想，在这么穷的社会里面你建好的高速公路有没有车在上面跑啊？基本没有，没有车跑的高速公路就叫做产能过剩的高速公路，所以A先生一旦做了第一个选择 之后，就会面临另外一个问题：严重的产能过剩。<br />
<br />
他生了两个病，由于他生了两个病，他望着苍茫的大地，10个矿工都离开了，一个杳无人迹的地方，只有一条耀眼的高速公路，上面没有车跑。他面临产业环境恶化、产能过剩，那么他手中剩下的50块能干什么呢？他只有盖一栋豪华别墅，50块钱一栋。<br />
<br />
这就是做第一个选择的必然结果，而且房子贵得要死，其他产品也会非常贵。<br />
<br />
这个简单的故事将贯穿整个演讲，你会发现目前中国的现状就是因为我们做了第一个选择，叫做什么？叫做“保八”。保八的结果一定造成两个困境，而两个困境一出来之后你就发现企业家没有地方投，只有炒楼、炒股，从而造成去年的回暖。<br />
<br />
而我一开始演讲，念给各位听的温家宝的谈话已经告诉各位产能过剩了，这就是这一个例子告诉各位的结果。你们也都知道，我们的投资环境日益恶化，两个病都出来了。那么我今天讲的是比较长的演讲，我现在就在这个前言的基础之上，我和各位朋友谈一谈我今天演讲的主题。<br />
<br />
下面演讲正式开始。<br />
<br />
美国对付伊拉克、阿富汗或者未来的也门都是传统的战争，可是对付亚洲国家一律是汇率大战。<br />
<br />
比如1985年的日本，那个时候日本格外嚣张，日本说GDP要超过美国了，甚至日本还扬言要买下美国，好嚣张。其实他们已经买了2/3夏威夷的地产，甚至日本还说我们也可以对美国说不。<br />
<br />
1985年，美国针对日本的汇率大战立刻开打。当时美国联合了德国、法国、英国总共4个国家，压迫日本签下了“广场协议”。该协议要求日币升值。从1985到1987年日币升值了一倍，严重打击了日本商社。<br />
<br />
到了1987年年底，美国再次投下第二颗炸弹，那就是逼迫日本降低利率，日本利率跌到当时最 低。这个炸弹太可怕了，因为1985年到1987年两年之间的日元升值，日本各大商社遭到重大困境。到1987年第二颗炸弹丢下之后发现利率这么低，因此 日本各大商社开始向银行借钱炒楼炒股。<br />
<br />
1990年1月份开始，美国投下第三颗炸弹，炸毁了日本的资产泡沫，让日本陷入了长达20多年的萧条。<br />
<br />
各位朋友记住，汇率大战一定要配合资产泡沫化才有效果，不知道你们听我讲这句话，心中有没有一丝恐惧？因为我们已经有资产泡沫了。<br />
<br />
1997年亚洲有个四小龙，还有四小虎，这八个国家和地区也是嚣张的不得了。我们什么也没 有，就是有点钱，结果美国趁着95年开始的亚洲资产泡沫化的天赐良机，1995年狙击亚洲货币，贬值一半，摧毁了泡沫，结果杀死了四小虎，杀伤了两只小 龙。要不是1997年中国政府出手搭救香港同胞，香港现在还是萧条的，可是其他国家地区就没有这么幸运了。<br />
<br />
