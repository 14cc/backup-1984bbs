													<h2>吴英这朵“恶之花”一定会成为祭品吗？</h2>
						
						
																											<div id="postmessage_418681" class="t_msgfont">非法集资的吴英一审被判死刑。一朵年轻美丽之花面临着可能过早凋谢的悲惨结局。学者秋风就此撰文说：“市场制度似乎也被判处死刑了”。从对一个个体生命的轨迹评说，跨越到对一项制度的演绎担忧，这当中包含了太多值得认真分析和品评的价值和逻辑。<br />
<br />
众所周知，尽管经历了30年改革开放，但，中国金融机构的官办色彩并未褪去多少。官办的低效率加上对民营经济要“慎贷”这种固有的“潜主观故意”，使得民营经济在获贷上举步维艰。于是，民间借贷暗潮涌动，“欣欣向荣”。本人就经常收到询问是否需要高利贷的手机短信，“上午签约、下午到款”，效率奇高，不能不令人怦然心动。而民营企业家们之所以自愿接受这种“暗交易”，是因为他们清楚地知道，与“官银”打交道时的付出（贿金、时间、精力）相比，民间借贷的利率实在高不到哪里去。高利贷之所以屡斩不绝，也实在说明它的风险并非“被说的”那么高。否则，民营企业家们岂非个个都成了傻瓜？<br />
<br />
然而，官方总像是不信上帝之法的法力赛人，把自己的理性力量估计得过高，以为只有人为设计于庙堂的制度才是合理的。在这种建构理性思维指导下，一切自发于民间的事务及其规则（自然演进之法），都被打压阉割取缔。殊不知，凡合理的、有生命力的制度多为经验的产物，起初和不断贯穿其间的风险是有的，但，优良的制度就是这么试错而来的。更为可怕的是，对自然演进之法除之而后快的心态和做法，还往往是出于维护庙堂规矩这种冠冕背后的政治意图。于是，为了贯彻某种政意浓厚的“官策”而不惜对个体采取所谓法律乃至人事手段，就成为一个不健康社会的常态。<br />
<br />
本人也曾关注吴英案。按我浅薄的法律知识，这起案件不可或缺的角色，理当有那些被吴英集资后未还钱的受害人，也理当看到他们要求还钱的民事自诉请求。遗憾的是，我自始至终只看到检察院这个公诉人。而且，吴英的罪名从最初的公司行为变为个人行为，审判机关也从当初的基层法院（无权判死刑）变为中级法院（有权判死刑）。这两项变更不能不让人怀疑存在某种意图将案件升级到维护某种“政治正确”高度以便杀一儆百的力量。<br />
<br />
写到这里我已经一身冷汗。惧定思惧，只要经济改革市场化的方向没有改变，在民间借贷必将合法化且似乎并非遥不可及的今天，吴英这朵“恶之花”一定会插上“自然演进成法”这一过程中的祭坛吗？假如她因法里法外的各种机缘巧合而有幸成为“命可保、牢必坐”的佘祥林，难道就会免却人们对可能在历史上留下遗憾乃至负罪感的担心吗？<br />
