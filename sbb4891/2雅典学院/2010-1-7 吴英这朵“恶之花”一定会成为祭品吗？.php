													<h2>南方人物周刊:慎刑应该是一个不坏的选择</h2>
						
						
																											<div id="postmessage_419033" class="t_msgfont"><a href="http://nf.nfdaily.cn/rwzk/20091228/gj/200912290002.asp" target="_blank">http://nf.nfdaily.cn/rwzk/20091228/gj/200912290002.asp</a><br />
给国家和人民造成170亿损失的德隆案主犯唐万新，只判了8年，甚至在服刑时一度被“保外就医”，和吴英相比，唐该是如何的幸运<br />
<br />
特约撰稿 马光远<br />
<br />
12月18日，传媒关注的浙江东阳吴英案宣判，吴英被认定构成“集资诈骗罪”，一审判处死刑，并处没收个人全部财产。吴英神话至此破灭。<br />
<br />
吴英，1981年出生，长相秀丽，只有中专文化水平，被控“从2005年5月至2007年2月，以高额利息为诱饵，以投资、借款、资金周转等为名，先后从林卫平等11人处非法集资人民币77339.5万元，用于偿还本金，支付高额利息，购买房产、汽车及个人挥霍等，实际集资诈骗人民币38426.5万元”。<br />
<br />
就涉案金额和操作模式而言，法院对吴英的定性及判决似乎并无不妥，奇怪的是，公众对于吴英案的判决结果，却表现出异乎寻常的关注，很多网友认为，吴英案判得太重了。而法律界人士围绕吴英案的定性的争议在一审判决之后也进入了白热化。<br />
<br />
自吴英案爆发开始，对吴英的“集资”行为如何定性，就充满争议，公诉方“集资诈骗”的定性与辩护方无罪的辩护意见更形成了鲜明的对比。出现如此巨大分歧的根源，在于我国《刑法》对“非法吸收公众存款罪”与“集资诈骗罪”规定的含混不清。<br />
<br />
中国《刑法》对脱离主流的金融供应体系而存在的集资行为，确立了两个罪名，一个是“非法吸收公众存款罪”，此罪因河北孙大午而国人皆知；另一个罪则是“集资诈骗罪”。<br />
<br />
这两个罪究竟如何区分，法理上的分野很清楚，但一到现实，却遭遇了难以区分的尴尬。<br />
<br />
就吴英案而言，疑点之一，是吴英集资的钱基本用于公司经营。比如，2006年3个月内，她以前所未闻的速度和手笔，置下大宗资产，购买100多间商铺，注册8家实业公司，如果其集资为了“占有”，大可不必搞如此巨大的固定资产的投资；<br />
<br />
疑点之二，最高人民法院在认定集资诈骗的标准时规定了七种情形，但这七种情况似乎都和吴英无法直接挂钩；<br />
<br />
疑点之三，吴英案虽然金额巨大，但其集资的对象却只有11个人，而且都是亲朋好友，其社会危害和影响是可控的。<br />
<br />
如果按照2008年12月浙江省高级人民法院等机构出台的纪要，吴英案只能算做普通的民间借贷纠纷。再加上浙江一带民间的高利贷借贷形式非常普遍，如果将之定性为集资诈骗，并且适用最高刑，的确有很大的争议空间：如果定“非法吸收公众存款罪”，则最高刑不过10年，而一旦定性为“集资诈骗”，则是死刑。<br />
<br />
据说，吴英多次表示，自己想做生意，又没有钱，银行又不贷款，只好通过民间借贷的方式。这多少反映了被排除在主流金融秩序以外的民间创业者的无奈，而在民间借贷又非常普遍的浙江，通过高利的方式获取资金事实上并不背经离道。<br />
<br />
其次，吴英并没有恶意地向社会公众吸收存款，其集资对象是固定的11个人，尽管账目不清楚，但其不能归还的3亿多，除了期货亏损等之外，跟前期已向这些集资对象支付了高额利息有关。<br />
<br />
而且她并没在发迹后向银行骗贷，对这些集资对象而言，真正的损失并没有那么大，所谓的“对国家和人民的财产造成巨大的损失”，似乎有夸大其词的味道。因此，外界对吴英死刑判决的争议，远远超越了法律本身。<br />
<br />
由此，笔者不由得想起建国以来最大的金融证券案件德隆案，其罪名是“非法吸收公众存款、操纵证券市场价格”，这个给国家和人民造成170亿损失的大案主犯唐万新，却只判了8年，甚至在服刑时一度被“保外就医”，相对于吴英，唐万新该是如何的幸运。<br />
<br />
既然法律模糊，既然中国金融的知识谱系本身处于一个不能自圆其说的扭曲状态，既然被主流制度视为非法的民间借贷却在尴尬地承担着民间资金主要供应者的重任，血祭一个小小的吴英究竟是维护法治的尊严，还是凸显制度的虚弱？<br />
<br />
笔者宁可相信，一个80后的年轻人，只是基于年少轻狂和不知天高地厚才触犯法律，对此却给予剥夺生命的重罚，价值和意义何在？慎刑应该是一个不坏的选择。</div>

							
							
							
							
