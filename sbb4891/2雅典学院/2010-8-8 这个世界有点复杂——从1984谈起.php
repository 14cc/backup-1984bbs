													<h2>这个世界有点复杂――从1984谈起</h2>
						
						
																											<div id="postmessage_775283" class="t_msgfont">看一般的政治哲学特别是自由主义的一套理论,容易让人产生天下大同的幻觉. 与此相关的当然还有绝对伦理学,理性思辨的传统等等,环环相扣，对于一个数学出身的读者来说真是不亦快哉.哪怕是西方的性恶论,也是很好地被束缚在宪政民主的框架之中,显得非常和谐. 它的特点是把一些良好的愿望作为给定的真理,然后用理性演化出一个好的制度来,尽管这其中也不乏天才的创造和世俗的权衡. 最难能可贵的是,它在美国得到了很好的运用.<br />
<br />
问题一：有人会说,美国哪是什么自由主义?还不是WASP的自由,别人的奴役.<br />
<br />
于是我们想起1984里面说,“有史以来，大概自从新石器时代结束以来，世上就有三种人，即上等人、中等人、下等人。……上等人的目标是要保持他们的地位。中等人的目标是要同高等人交换地位。下等人的特点始终是，他们劳苦之余无暇旁顾，偶而才顾到日常生活以外的事，因此他们如果有目标的话，无非是取消一切差别，建立一个人人平等的社会。这样，在历史上始终存在着一场一而再再而三发生的斗争，其大致轮廓相同。”这一般来说是一个金字塔的社会结构，下等人占了社会的绝大多数，供养着上面两个阶层。放在中国古代就是农民，乡绅和朝廷的划分。（这里说上中下等人，完全是方便讨论，没揶揄人的意思~~）<br />
<br />
我们不得不承认这个“阶级斗争”的现象普遍长期存在，但是美国却让情况略有改变。技术的进步和再分配方式的改善使得中等人成为了大多数，就是中产阶级嘛。他们成了大多数之后就不再可能都去追求权力，毕竟当官的是少数，因此他们就喜欢代议制，通过选举来间接控制权力。于是他们成了民主的主要力量，他们的利益或多或少都得到了保障。搞代议制的竞选渐渐演变成党派政治，也是资源之争，被选上的都是资源调动力强的人，所以上等人依然存在。但是他们不会明目张胆地乱搞以犯众怒，他们的地位虽然牢固但是也并非绝对。下等人同样存在，比如非法移民，少数族群等等，但是他们也偶尔受到中产阶级的同情。他们人数不算多，社会不是靠压榨他们来发展，因此处境远没有从前那么恶劣。因此我们可以说美国缓和了阶级矛盾，并使得自由主义成为了主流观念。<br />
<br />
要说美国不是自由主义倒可以这样论证：自由主义已经成为了一种社会的意识形态，以致于别的与之不同的主义都受到了不同程度的打压，这种极端的姿态违背了自由主义本身的宽容传统。<br />
<br />
问题二：中国能走这条路吗？<br />
<br />
我们可以清楚地看出，中国还处在1984所描述的阶级分化中。虽然技术进步，中国还是没有能够让大多数人解放出来成为中产阶级，经济发展还是靠农民工等廉价劳动力支撑。下等人依然是占据了大多数。中产阶级非常稀少，地位也非常不稳固。他们一方面被政治的统治阶级束缚（行政权管理了言论，税收，资源），一方面被经济的统治阶级剥削（比如房价让大多数中产阶级变成无产阶级）。他们在希望民主的时候难免有两种意识：一是处于下等人的边缘而要求自由平等，反对剥削的本能；二是把上等人取而代之的冲动。这两种意识显然都不是民主发生的正常路径。<br />
<br />
有一种观点是我们慢慢等吧，等城市化把农民都搞进城，等产业升级创造更多的中产阶级，然后就有希望了。这不能说不是一个办法，不过夜太长，梦太多。中国真的能产业升级创造无数的工作岗位吗？廉价劳动力转移到其他发展中国家，中国更有可能是从产业链条中出局而不是上行，不然你上来原来的在上面的还能让你挤下去？不是说没有机会，是搏杀很残酷，胜负很难料。其次，上等人会眼睁睁看着中产阶级坐大吗？1984告诉我们，不。但苏联不也是解体了嘛，还是上等人采取主动，所以也不是没有机会，是胜负很难料。<br />
<br />
另外一种观点，既然现在中等人不行，靠下等人如何？1984的主人公就是这个想法。毕竟他们是大多数，对人人平等也肯定是支持的。不过如同上面引文所说，下等人并没有那么强烈的政治诉求，维持生计就很满足了。这可以找出无数的印证：于建嵘说中国农民没有权利意识，只是在自己的最基本的权利（而且主要是财产权）受到巨大侵犯的时候才会产生抵抗，而这些问题大多数都是可以用钱摆平的。茅于轼说底层民众的温饱大多得到解决，因此真正贫富之间的效用差距并没有实际数字差距那么大。可以推测当温饱得到解决之后下等人的不满就不会那么激烈。所以他们并不会太热心民主，除非有一天吃的喝的呼吸的全部有剧毒，房价又太高他们只能住贫民窟，他们的政治热情才有可能被激发起来。<br />
<br />
1984还提到两种可能，被外族入侵而改变（比如伊拉克），或者统治者自己丧失了统治的信心（比如苏联）。前面一种可能比较惨烈，大多数人都容易受苦，国家的利益也难以保障；后面一种倒是方便，但是当党不再是由几个人而是一个巨大的利益分散的集体掌控的时候，这种可能性就几乎为零了。这两种方式能不能带来我们期望的民主那更难料。<br />
<br />
问题三：中国民主之后能比现在更好吗？<br />
<br />
刚才那个问题都没戏，这完全只能是YY。最近看《让民主运转起来》，里面有大量翔实的数据分析得出结论：民主的成功和效率（以及伴随的现代经济的效率）是要靠公民共同体来保障的，而且这种共同体文化难以人为在短期影响。什么是公民共同体？就是一个社会的横向合作，成立五花八门的经济的，娱乐的，有时也包括政治的社会组织以及由此建立起来的社会资本。和它相对的是纵向的附庸-庇护关系，类似于派系，每个人孤立地与一个更高一级的人之间联系，以取得保护。当然还有一种就是极端的个人主义，两个方向的关系都没有。<br />
<br />
很可惜，中国是附庸-庇护关系更发达的社会。政治上最重要的就是站队，站对了就对了，不然就完蛋。民间的横向合作很少，以前有一个大的家族，可以互相帮忙，是合作的一种，现在被铲除了。我们发现江浙一带的社会横向联系更为紧密，有各种相互支持的经济发展模式，以温州人最为有名。他们在全国的领先也印证了书中的结论。所以中国民主之后政府要变得更好，更有效很困难，但不会那么作威作福。<br />
<br />
问题四：自由主义是普世价值吗？<br />
<br />
我说过很多次，价值都是相对的，没有什么普世价值。从上面我们也可以看出，自由主义在中国发展有困难，也不一定是最有效的（当然我不是说它不可能或不值得借鉴）。性恶论并不能完全被民主所接纳，所以产生了马基雅维利的君主论和中国法家的权术。当性恶成为常态之时，道德就难以发挥作用了，取而代之的是明争暗斗。就像上一篇说的，人性的恶成为统治者得以利用的弱点。中国的德治是全力地推崇善，以取得和谐。这是大方向，但是社会经常没有形成善的资本，所以不得不用权术来补充。<br />
<br />
