													<h2>你愿意为民主自由付出什么代价？</h2>
						
						
																											<div id="postmessage_393464" class="t_msgfont">很多人说愿意为民主和自由献身，热血沸腾的多了去。我想问个问题：正常的普通人到底愿意为自由和民主付出什么代价？<br />
<br />
假设你面临着这样的选择：在现在的专制中国和民主自由的中国之间选一个，但是选择民主自由中国需要你付出额外的代价，你愿意付出什么代价呢？这个代价，就是对你而言，自由民主值多少钱。<br />
<br />
这里的假设是：民主自由中国的生活水平和现下的中国一样，你的职务、社会地位、工资收入、物价、社会治安环境等等都跟现在的中国差不多，除了有民主自由。<br />
<br />
如果你割掉鸡巴，你就有了民主自由，你愿意吗？<br />
<br />
如果你砍掉一只手，就有了民主自由，你愿意吗？<br />
<br />
如果变成了瘸子，你就有了民主自由的祖国，你愿意付出这个代价吗？<br />
<br />
如果你瞎掉一只眼睛，就换来了民主自由，你愿意吗？<br />
<br />
如果你毁容了，就有了自由民主，你愿意用如花容颜换取民主自由吗？<br />
<br />
也许肉体的代价太高，会让人下不了决心，现在说金钱的。<br />
<br />
你愿意为了民主自由付出一生收入减少一半的代价吗？<br />
你愿意为了民主自由付出一生收入减少三分之一的代价吗？<br />
你愿意为了民主自由抛弃你现有的房子吗？<br />
你愿意花10万块钱买民主自由的生活吗？<br />
<br />
我的意思是，除了民主自由，没有别的东西，没有任何人知道你付出的代价，没有任何人感谢你，你不会获得任何荣誉，连你的亲人都不知道也不相信你是为了民主自由付出这个代价的，那个自由民主的社会人们有权肆意嘲笑那些为民主自由做贡献的国父们，甚至谩骂他们，在他们看来，民主自由的社会实在太乱了。而你，也是一个默默无闻的庸人，你永远也不可能靠民主成为精英，因为没人会看得起你而投你的票。<br />
<br />
然后再思考这个问题：你愿意为了民主自由坐牢几年？三年？五年？十年？二十年？当然前提是一样的，这个徒刑只是让你坐牢，是惩罚性的，不是表彰性的，没人知道你为民主自由坐牢的事儿，人们也不会因为别人为民主自由坐牢额外尊重你或更看得起你。比如说那个时候的刘晓波如果患了老年痴呆，没了稿费和其他任何收入，在大街上乞讨要饭，别人并不因为刘晓波为民主自由做出很大的贡献、坐了很多年牢而给这个乞丐多一分钱的施舍。坐牢当然不是娱乐，没有让你慷慨激昂在法庭上表演豪迈的机会，没人会被你打动感化，也不给你皮鞭、镣铐、老虎凳、辣椒水，这些东西太有革命英雄主义的浪漫审美价值了。他们只是把你扔在一群刑事犯中，狱霸会逼你给他叩头，喂你吃大便，甚至鸡奸你，你要在这个牢房跟这些变态凶残的罪犯合住几年。<br />
<br />
我之所以限制这么残酷的条件限制，只是避免调查的误差。如果一个人为自由民主奋斗会得到额外的尊重，那么他愿意付出的代价不是为民主自由本身付出的代价，而是为民主自由加上额外的尊重、青史垂名、革命英雄主义享受这些东西的总和而愿意支付的价格。<br />
<br />
而我感兴趣的是：剔除一切额外的，你愿意为自由民主付出什么价格。不要说民主自由是无价的，我觉得它是可以用价格来衡量的。<br />
<br />
当然会有人说我的假设根本不存在，只要民主自由了，收入怎么会减少了，人们怎么可能会不尊重民主英雄呢……<br />
 抱歉，我不会跟你讨论这个问题，我只是想了解：你愿意为了民主自由本身付什么价格，而不是带有额外赠品的自由民主。</div>

							
							
							
							
