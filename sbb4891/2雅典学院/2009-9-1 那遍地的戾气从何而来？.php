																											<div id="postmessage_222647" class="t_msgfont">从前有人说我天朝下遍地是戾气，这点不假，可那时候人们总把这些戾气归咎于达官贵人，或者市间小吏。仿佛有朝一日这些人都没了，那股戾气自然化解。<br />
<br />
<br />
<br />
不过我倒觉得，这股戾气远非土豪劣绅贪官污吏这八个字能够囊括，反倒是具有一些普遍性，不光是他们的责任，而是每一个人的责任。就好象我朝官方宣传任何事件一般：总有那么一小撮坏人制造了事端，大部分人都是好的，没有责任的-----不光是时下，就连对前辈们，比如十年动乱之类的，也是如此定义。一来人们因此怡然自得，觉得自己没什么责任了，都是他们不好么，一来又养成了这么一种习惯：一旦出了事情，任何事情，那都是他们不好。<br />
<br />
<br />
<br />
说到这里，我不得不又翻出了自己的老话：那些看似扯淡的宣传，舆论，所有人都不信的那一套，可实际上已经渗透到大多数人的内心深处并且获得了认同，即便是对他们的批判，往往用的也是他们的那一套。本质上，这个社会的大多数已经成了一张面目的了。<br />
<br />
<br />
<br />
小到买个西瓜，或者互不相识的人偶发的磕磕碰碰，大到突发事件，天灾人祸。处事最不好的自然是无理取闹千夫所指，但是得理不让人有如此规模的市场却只能说是一个粉饰文明的野蛮社会实质体现。<br />
<br />
<br />
<br />
在出现了矛盾之后人们首先想到的不是对话，而是斗争。斗争并非不必要，但斗争往往是最后的手段，而暴力则是斗争的最后手段，不到万不得已自不能轻易动用。可恰恰这些最后手段往往在一开始就被亮了出来，或者那种自称是对话，实际上却是斗争的做法，从官方到民间，此惯例无处不通。<br />
<br />
<br />
<br />
弱势者往往抱怨对话的门路受到阻塞，可是人们可曾想过，当自己站在了强势的地位时，可曾向那些弱势者把对话之门敞开？说得严重点，仗势欺人似乎已经被习以为常，这里的势不光指金钱，地位，也包含了“得理”一词。西游记里有段俗话，叫依着官法打杀，依着佛法饿杀。在这样的世界里，市民们得理之后能将一个活小偷打成一个死小偷，城管能将一个活游客打成一个死游客，暴民可以砸店，不体面的人看着体面的人落水会拍手称快（始于阶级斗争理论），朝廷可以把所有的对话门路堵死之后再指责敌对势力亡我之心不死。<br />
<br />
<br />
<br />
