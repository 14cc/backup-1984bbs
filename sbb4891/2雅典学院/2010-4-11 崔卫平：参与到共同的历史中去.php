													<h2>崔卫平：参与到共同的历史中去</h2>
						
						
																											<div id="postmessage_559123" class="t_msgfont">参与到共同的历史中去 <br />
<br />
&nbsp;&nbsp;<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;首先，请允许我感谢东道主，感谢在座的各位同行，因为你们的慷慨，使得我虽然不能到场，但是仍然能够发出自己的声音。<br />
<br />
&nbsp; &nbsp; 就会议“遗产”这个话题，我想谈谈自己所继承的“遗产”，以及它们与现在的联系。它们也正好体现在这样几个故事当中。 <br />
<br />
&nbsp; &nbsp;2009年5月10日，钱理群先生、秦晖先生、徐友渔先生与我等二十来个朋友，我们在北京召开了有关1989年“6・4”的会议。我们觉得二十年了，不能继续停留在对于此事的“官方定论”当中，应该发展出不同的表述，尤其是不能继续将这么大的事情，从人们的记忆中尤其是公开谈论当中抹杀。<br />
<br />
&nbsp; &nbsp;对于我个人而言，“6・4”这件事情实际上构成此后思想和工作的重要起点。那就是：能够打碎我邻居头颅的子弹，同样也能够将我的脑袋射穿。<br />
<br />
&nbsp; &nbsp;于是我需要找到另外一些概念和语言，它们能够承担起这种裸露的、毫无庇护的处境，能够与这种无权的易受伤害的处境相匹配。这种处境是我的，也是千千万万普通人们的。如何将这种被取消的、暗哑的处境，转变为可以看见、可以听见因而可以被恰当了解，这是一个需要面对的难题。<br />
<br />
&nbsp; &nbsp;在这个意义上，将“6・4”称之为“遗产”是不恰当的，实际上它从来没有离开过身边的人们和我，人们以不同的方式在承受这个难以承受的事情及其后果。至今遇难者母亲们不能在公开场合中露面，压制母亲们的力量肯定也在压制其他悲伤的面孔。在大街上针对手无寸铁的人们所施行的暴行，对于我们民族的影响是深远的、难以估计的。 <br />
<br />
&nbsp; &nbsp;第二个故事是这样的。2009年12月底，主要是因为零八宪章而被捕的刘晓波博士，在被关押一年之后开庭。在庭审宣判的前一天（12月24日）晚，我开始电话及email访问知识界的一些同行，从刘晓波的博士生导师到他的同班同学，开始是一些与他直接打交道的人们，后来扩展到知识界的各路人马。我给自己所确定的访问对象是从八十年代一路走过来的人们。<br />
<br />
&nbsp; &nbsp;为什么是八十年代？在我看来，“零八宪章”中大部分话题，都是在那个时期、不同的人们在各种场合谈论过的，而同样在1989年之后，它们被强行中断。无论如何，这是我们一段共同经历的道路，是一段共同的历史记忆，需要对我们自身的历史，对自身走过的道路，尤其是那些生长点、当年那些新的思想空间，抱有一种忠诚和忠直的态度。<br />
<br />
&nbsp; &nbsp;实际上，被称之为“拨乱反正”的年代，也是我们这一代人从中生长、获取对于世界基本眼光的年代。尽管它有许多不成熟，后来的形势也发生了很多变化，但是它的一些基本命题――比如有关“社会进步”、“总结历史经验教训”、“反对专制和特权”，尤其是那样一种反思和批判的精神，以及借助历史来看待现实的眼光，奠定了我们日后的一些重要思想基础。忠诚于这样的起点，便要求人们不要急于更换“我们的世界图景”，乃至让那些沉入地下的人们，经过我们自己之手，陷入永久的沉默。<br />
<br />
&nbsp; &nbsp;我为这个“红色遗产”会议提交的论文关于八十年代人道主义与异化问题的讨论。这是八十年代的一个理论亮点，是在马克思主义理论的名义之下进行的。争论的焦点包括――马克思是从“社会关系”出发还是从“现实的人”出发；马克思主义仅仅是解释历史还是包括对于历史的评价，即是否体现一种价值观在内；以及有关“物的价值”还是“人的价值”、马克思所批判“商品拜物教”“货币拜物教”如何在中国演变成了“权力拜物教”，甚至包括对于现代化最初的反思，环境和生态的问题也提了出来。争论的一方为当时的人民日报副主编王若水先生、刚从劳改农场回来的高尔泰先生等，另一方面则是当时的中宣部长邓力群。争论的结果是权力大过真理，王若水被从《人民日报》免职，1987年被从党内除名，至今他的名字很少在公共场合中出现。 <br />
<br />
&nbsp; &nbsp; 第三个故事是最近发生的。今年3月3日与4日连续两天，我被要求不在3月5日这一天，去三联书店的二楼和宋庄美术馆，因为在这两个地方有举行纪念遇罗克的活动。四十年前的这一天，遇罗克被判死刑并执行，时年27岁。因为他写了一篇《出身论》的文章，驳斥了文革初期流行的“老子英雄儿好汉，老子反动儿混蛋”的“血统论”。安放在宋庄的遇罗克雕像上面刻着他这句话：“任何通过个人的努力所达不到的权利，我们一概不承认。”这样一种来自权力中心和体制之外的平等诉求，为官方所不能容许。<br />
<br />
&nbsp; &nbsp; 时隔四十年，不允许纪念遇罗克这件事情，令人无法接受。一个已经成为雕像的人，他的危害性何在？1979年11月底，北京市中级人民法院宣告遇罗克无罪，当时不止一个大媒体都发表了长篇通讯，介绍遇罗克的事迹。难道过了三十年，这个人的罪名又复活了？这从另外一个角度证明了所谓“遗产”的现实性，在那遥远的岁月深处，金子般的思想仍然在闪耀着不可磨灭的光芒，对当下仍然具有强大的穿透力。<br />
<br />
&nbsp; &nbsp;结果我的朋友与我在3月4日这一天（即提前一天），赶赴宋庄，一起在遇罗克的雕像前献上花篮和我们的献词。这样做是为了表明一种公开的态度：参与到我们曾经的历史当中去，担负起我们共同的记忆，包括那些不光彩的事情。在这样的历史面前，也许我们一个人都是有原罪的，也许每个人不同程度都是有负担的。 <br />
<br />
&nbsp; &nbsp;可以说，这部历史不管有多么晦涩，有多么痛苦，多么令人不堪回首和感到羞耻的地方，但是，那就是我们的过去，是我们的前人留下来的这个世界，我们直接继承了它，我们在它所提供的条件之下生活和成长，在它规定的范围之内学习和得到训练，我们不可能有另外一个出身。所有发生过的事情，也不可能通过一番别的阐释，而将它们彻底改写。<br />
<br />
&nbsp; &nbsp;当那些悲惨的事情发生时，我们的前人也许没有足够的智慧，不能令人效仿，不能给其他民族提供参照的东西，这的确有些丢人，有些令人难为情，但在今天，这个不是最重要的；更加重要的是，今天的我们，是否愿意正视这样的历史，从中汲取恰当的教训，尤其是建立和发展出一些基本的界限、尺度和视野，牢牢建立起一些基本警觉，由此而开始走上一条智慧的道路。 <br />
<br />
&nbsp; &nbsp;也许，对我的耳朵来说，谈论“遗产”有些奢侈，或者这个话题有些太早――被认为是过去的东西，实际上许多仍然以“现在时”的方式正在进行，这就是我们身处其中的现实。而当我们在这里谈论，也都是以这样或者那样的方式，参与到这个现实中去，与它发生不同意义上的互动。<br />
