													<h2>预付人权：一种非西方的普遍人权理论</h2>
						
						
																											<div id="postmessage_345515" class="t_msgfont">赵汀阳&nbsp;&nbsp;《中国社会科学》杂志 2006<br />
<br />
关键词：天赋人权，预付人权，关系，公正，博弈论<br />
 <br />
提要：<br />
作者以人权理论的元理论分析而提出“预付人权”理论，并试图证明“天赋人权”在理论合法性上的失败,特别表现为它所包含的自毁逻辑。预付人权理论认为，每个人生来就获得人类预付借贷给他的与任何他人相同的权利，人权虽然不劳而授，但决非不劳而享，否则损害公正。因此，预付人权是有偿的，是有条件保有的。一个人获得并接受了预付人权就意味着承诺了做人的责任，并且将以完成做人的责任来偿还所借贷的权利。如果拒绝了预付人权所要求的部分或全部义务，就视同自动放弃了部分或全部人权。同时作者还分析了非西方国家在应对西方人权批评时的各种错误策略。<br />
 <br />
 <br />
 <br />
 <br />
 <br />
1．如何超越人权的知识政治学？<br />
 <br />
人权已经发展成为一个虽无宗教之名而有宗教之实的西方新宗教。人权成为西方新宗教标志着西方现代性的完成以及随之而来的终结（完成往往意味着终结）。现代以来，以人的主体性为基本原则的现代性消解了神的权威，尽管基督教在今天仍然是个具有相当力量的传统精神象征，但已经退化为次要的意识形态；自由主义虽是现代社会的主流意识形态，但特殊的政治偏向使它仅仅是“自由世界”的政治话语而难以成为普遍意识形态。这意味着，现代长期缺乏一种能够一统江湖的最高意识形态。现代的物质世界早已成熟，但其精神无主的状态表明现代性没有完成。在此背景下，人权观念巧妙地消化了基督教和自由主义资源而成为新宗教，人权在今天的地位几乎相当于基督教在中世纪的地位。人权实现了由思想向信仰的转变，可是当人权变成拒绝怀疑的信仰，它在思想上就死了，只剩下思想的尸体――信念。以人权为依据去批评各种事情就好象是不证自明的正确政治行为，而对人权的质疑也都好象变成天生不正确的政治行为。<br />
 <br />
单就理论潜力而言，人权确有条件被做成一个超越文化特殊性的普遍观念。但人权本来的学理性被宗教性和政治性所掩盖，变成西方用来攻击其它文化体系的一个政治理由。理论与政治的偷换弄脏了人权概念。在这里，我们试图纯化人权概念，消解人权的知识政治学，使之纯化为一个学理问题。<br />
 <br />
人权概念起源于西方，但对于一个被公共化的共享概念，历史背景不意味着解释的特权。假如一方面人权被认为是普遍的而非专属的；另一方面又把人权的解释权看作是西方专有的，这是无理的矛盾。既然人权被认为是普遍有效的概念，它就必须在理论上是开放的，在文化上不可以设限。假如人权只能按照西方偏好和标准去定义，人权就只是个西方的地方性概念，也就不能用来批评其它文化。因此，人权不是既定的而是一个允许对话和辩论并且可以重新解释和定义的公共概念。在“去历史化”和“去西方化”之后的人权才可能成为一个普遍概念。显然，人权在理论上有着多种可选择的可能含义，在所有可能的人权概念之中最好的那一个，才有资格成为普遍的人权概念，而那个“最好的”人权概念只能是学理上最优的概念。这是重新确定人权概念的唯一正确方法。<br />
 <br />
不过，在西方话语霸权的影响下，西方价值偏好所规定的人权概念成为目前流行的人权解释框架，世界各国由于没有发展出别的更好的人权理论，在人权问题上就只好默认西方的解释框架，于是，即使反对西方的人权批评，也只能在西方所规定的框架内去进行辩护，这样就事先受制于人，完全没有主动权，无论什么样的辩护都自陷于被动。默认对方给定的不公正解释框架是一个严重的政治失误，默认心灵受制于人就等于出卖心灵，等于签订了精神上的无条件投降条约。<br />
非西方国家（包括中国在内）缺乏自己创造的人权解释框架，就不得不默认并且受制于西方的解释框架，因此所能想到的对西方批评的反驳策略主要是这样几个类型：（1）试图证明自己国家的人权状况已经逐步得到某些改善，并没有像西方所批评的那么糟糕；（2）反过来指出西方也同样存在着尚未解决的人权问题；（3）提出某些别的人权项目，试图通过增加人权项目来削弱批评。<br />
 <br />
这样一些辩护策略并不成功，不仅被动，而且弱势。策略（1）是非常不成功的辩护，因为预先承认了西方标准并且承认自己有错误，尽管把错误解释为客观条件限制所致，那也仍然是错误，既不能把错的说成对的，甚至也不能把大错说成小错；策略（2）是最幼稚的辩护，而且不成立，因为别人的错误不能用来为自己的错误辩护，更不是自己也犯错误的合法理由；策略（3）相对来说有些积极意义，但微不足道，至多稍微冲淡西方色彩，因为所增加的新项目只是在承认了西方框架的前提下才是有意义的，而且，新增加的项目没有理由成为基本项目而只能是补充性项目，因此终究缺乏力量。以上几种常见的不成功辩护策略表明了非西方国家在知识论上的失败。<br />
 <br />
这些策略失败的一个知识论原因是错误地承认某种游戏是唯一可能游戏,因此把西方定义的人权看作是既定的游戏。这里涉及对游戏理论（在哲学中称游戏理论，在经济学和政治学中也称博弈论）的一个基本理解。维特根斯坦相信游戏的根本问题是规则问题，它是所有制度及其实践模式的哲学基础。但维特根斯坦忽视了另一个甚至更根本的游戏问题，可以称之为“游戏种类选择”问题（维特根斯坦对规则的技术性困难更感兴趣，而忽视了游戏的政治学意义）。游戏的选择是一个前规则问题，它是游戏的原始出发点。生活总要进入某种游戏，但在把某种游戏确定为共同承认的游戏之前，存在着一个创造性的原始游戏，它是“选择游戏的游戏”，在这个特殊游戏中，所有规则都是未定的，而且正是需要被选择和规定的，这意味着在理论上存在着多种可能游戏，它们都是同等资格的候选游戏。选择了某种游戏就是选择了某种政治。人权必须被看作是个未定的游戏，这样就能够把问题退回到原始游戏状态也即退回到理论起点进行重新反思。<br />
 <br />
 <br />
 <br />
 <br />
2. 可疑的特殊价值论<br />
 <br />
以学理去解构政治是拒绝话语霸权的最好方法，但有必要注意一种并不成功的学理策略，一般称作文化多元论或者相对主义策略。为了反对西方的人权批评，非西方国家往往以多元论或相对主义为理由去坚持自己的文化权利，例如亚洲有些国家的政治家和学者主张“亚洲价值”（类似的还有“伊斯兰价值”）去反对普遍主义价值观，其论证策略主要是强调价值观从属于文化，而不同文化之间缺乏可通约性。<br />
 <br />
H.Rosemont有个分析对于理解相对主义和多元论或有帮助：相对主义是同样的文化/知识体系中人们对问题的不同看法，至于不同文化/知识体系之间的不同看法则是多元论的，不同的文化/知识体系甚至有完全不同的问题。他举出的例子是，中国文化用来形成问题的词汇表就完全不同于西方，因此根本没在讨论与西方同样的问题[2]。在同样知识体系中对同样问题的不同看法是相对主义，由于所利用的知识资源相同而只是偏好不同，所以相对主义的特性是“无争辩的”；而多元论则是不同文化/知识体系各有各的问题，其特征是“不可通约”。非西方国家在今天的思想处境实际上兼有多元论和相对主义双重倾向，非西方国家既求助于多元论又求助于相对主义来回应西方的思想挑战，因为文化不可通约理论不足以回应全球共同和普遍的问题，这样就发展出“亚洲价值”等特殊价值论。<br />
 <br />
亚洲价值的主张从一开始就受到许多质疑。首先“亚洲价值”是一个虚假概括，正如阿马蒂尔.森指出的，想要概括亚洲丰富多样的文化是不可能的而且是“极其粗鲁的”，把亚洲简单地看成一个同质单位，这本身就是欧洲中心论的看法[3]。森甚至认为，诸如“民族”、“文化”之类根本就不是理解和分析思想和政治差异的合适单位，在“西方文明”、“亚洲价值”或者“非洲文化”之间人为想象各种差异往往掩盖各种文化自身真正的意义[4]。亚洲价值的概念固然混乱，但它多少是个有号召力的政治策略。可是“亚洲/西方” 这一划分――确如森所指出的――正是西方的一个思想陷阱。以“亚洲”去反对西方，表面上似乎是对话语霸权的解构，但这一消极抵抗的态度决非最优方法，甚至只是一种耍赖策略。从全球化以及历史长时段去看，地方主义或特殊主义终究弱于因此不能抵抗普遍主义，因为地方主义不准备为世界着想，不准备为所有人负责，更不能回答人类必须共同面对的普遍问题，因此缺乏普遍意义。哈贝马斯就很清楚这一点，他指出人权要求的是超文化的普遍价值，而“亚洲价值”只不过是个特殊对策而不是普遍规范或原则，因此是文不对题的[5]。当然，西方也有不少学者支持文化多元论或相对主义，但值得注意的是，文化多元论只说明文化是不同的，却不能逻辑地推出各种文化是同样好的，例如罗蒂虽然乐意承认文化多元论，但他仍然傲慢地认为，根本就不需要去证明人权的普适性，推行西方人权观念的理由仅仅在于西方的“人权文化”在道德水平上“本来就是更为优越的”[6]。罗蒂这一令人作呕的文化种族主义倒也说明了一个致命的问题：特殊价值论其实对强者有利而对弱者不利。因此，鼓吹文化特殊价值论决非优选策略。<br />
 <br />
如果一种价值观不承认普遍主义，就没有希望成为普遍规范，就没有资格对世界问题说话。普遍主义价值观未必总是对的，但它却有应对普遍问题的气度、设想和责任感，这就是普遍主义的“普遍性”资格。地方主义虽然可以是一种文化保护主义策略，但无法应对普遍问题，因此，地方主义并不能真正解构话语霸权，最多是对话语霸权的一种未必有效的设限，而霸权仍然还是霸权。要真正解构话语霸权，唯一有效的做法就是去发现更好的普遍主义理论。有一些似乎对中国思想有好感的西方学者如Louis Henkin认为，与西方价值一样，其实“亚洲价值也是普遍价值”[7]。这是很有见地的，不过仍然不能回答全球化所提出的普遍价值问题。各种文化之间存在着许多不可兼容的差异和冲突，如果把各种价值都说成是普遍价值，或许有助于互相尊重，却不能改变和解决任何问题，而全球化已经给定了普遍交往和合作的要求，于是必定需要一种能够获得普遍认可的并且能够解决共同问题的普遍价值体系。这样一种普遍价值体系目前尚未存在，它必须被构造出来。<br />
 <br />
 <br />
 <br />
2．西方人权概念的地方局限性<br />
 <br />
人权是典型的现代观念，现代观念又被认为是经过理性批判而建立的超文化观念，而超文化就同时是超历史的，这是现代普遍主义的梦想。以康德为典型代表的现代哲学家最理解理性的力量：只有理性才具有“普遍形式”，因此才能把某个观念整成普遍观念。但西方人权承载着太多的西方文化特殊偏好，并不是理性所能够消化得了的。我们可以通过检查西方人权所必需的一些基本假设，把伪装成普遍知识的西方人权概念还原为一种地方信念。<br />
 <br />
自基督教取代希腊哲学成为西方的精神主导，西方在价值观方面就再也没有越出宗教格式，人权就是西方的现代宗教。启蒙对神权的否定虽然导致了传统宗教的衰落，却继承了基督教的许多假设，只不过把神的宗教变成了人的宗教。新教以来，基督教不仅肯定了在上帝面前人人平等，尤其还肯定了每个人与上帝直接交通，这样就把平等的信念落实为平等的权利。来自基督教的平等观念成为现代性的重要基础，它至少支持了这样几个现代信念：（1）每个人不依赖于他人的独立价值；（2）每个人的平等价值；（3）每个人的个体价值相对于他人的绝对地位。因此现代性可以追溯到基督教，至少到马丁.路德。正是基督教把“人”这个整体单位真正分化成为个体。在此之前，虽然有“个体”，但个体总是属于共同体的，还没有完全独立的意义，而基督教通过建立每个人与上帝的直接神圣关系而压倒了人与人的世俗关系，于是每个人都从他人那里独立出来了成为“他自己”。在所谓“上帝之死”之后，已经独立于他人的个人甚至与上帝的关系也不再真实，人虽然孤零零，却升格为绝对价值。“个人”这一存在论单位与“个人权利”这一伦理学价值的结合完成了现代性的基本结构。个人的绝对价值正是西方人权的必要假设和根本意义。<br />
 <br />
在价值上独立自足的个体概念是经过基督教和现代启蒙两道加工而形成的，这其中的宗教背景和现代性背景都是西方特殊的地方文化，只不过西方试图把它们推销成普遍观念。西方文化的成功传播更多地依靠了武力、殖民和霸权，而很少由于他者的主动仰慕而得到成功传播，这一点说明了西方文化并没有普遍魅力而只是一种地方偏好。在世界的其它文化里，甚至包括基督教之前的希腊文化，在关于人的理解上都没有如此突出个体的优先地位和绝对地位，而是更重视共同体的意义（家庭、城邦、部族或国家等）。<br />
 <br />
从理论上说，“个人”是神学的一个存在论虚构，它并不存在于真实生活中，而只存在于神学生活中。神学生活是一个只有“人与上帝”以及“上帝与魔鬼”这样单调关系的可能世界，因此人可以仅仅以“个人”这一单调身份与上帝交往。可是在世俗世界中，“个人”这一身份显然过于单调以至于无法表达丰富的生活内容和意义，而且人与人的关系比人与神的关系要重要得多，事实上每个人的各种生活意义都不得不在与他人的关系中被定义，或者说，人的意义不可能还原为个人概念去说明。西方人权文化的背景是神学文化，这一不协调的背景使它具有悖谬性的后遗症，它所设想的人的概念只在某个可能生活中有意义，而不是一个对各种可能生活普遍有效的人的概念。一个普遍理论必须能够把各种可能世界和可能生活计算在内，而一种文化传统却只能表达一个特殊的可能世界及其可能生活。<br />
 <br />
 <br />
 <br />
 <br />
4．人权论证的有效策略<br />
 <br />
既然目前的人权概念并非普遍有效，我们就需要重新构思人权概念。要论证一个有普遍意义的人权概念，其论证策略就必须把真实世<br />
的所有可能生活考虑在内，以我目前的考虑，这样一组论证策略应该是合适的：<br />
（1）&nbsp; &nbsp; 要定义一个普遍有效的人权概念，就必须把所有可能生活考虑在内，即把各种可能出现的行为策略考虑在内，而不能仅仅代表某种地方生活。<br />
（2）&nbsp; &nbsp; 给定理论T，那么必须考虑T所需的防护能力，或者说，T是否能够承当得起T所带来的可能后果？T是否有能力应付T所可能导致的各种问题？这是个“理论担当问题”。人们对某种观念如此偏爱以至于往往只看到好处而忽视其后果。这个问题也可以看作是一个博弈论问题：理论T相当于给出了一个制度策略，于是就不得不把人们的各种可能的反应策略考虑在内。<br />
（3）&nbsp; &nbsp; 理论T必须获得存在论的支持，即T所承诺的事情必须是真实世界所能够支付的。这是一个“理论兑现问题”。这个问题也很容易被忽视，人们往往只考虑到一个主张是不是“好的”，而没有考虑到所要求的或所承诺的事情是否是真实世界能够支付得起的。事实上，世界所能够支付的“好事情”远没有人们希望的那么多，而且，在很多情况下，人们的各种要求之间互相矛盾或者互相消解，从而减低了世界的支付能力。<br />
 <br />
人权理论是一个非常“入世”的理论，因此必须把生活条件以及各种或许出现的可能生活计算在内。这样的“现实理性”论证策略显然比传统的“概念理性”论证策略更为谨慎。试图仅仅在概念演绎中完成合法性论证的传统方法已经变得非常可疑。最典型同时也最有影响的是康德的伦理学论证，它往往被看作是人权的纯正理论基础。康德相信通过对理性自身的分析就可以必然得出普遍道德原理，其主要成果是“绝对命令”的一般公式和“人是目的”的最高价值观。康德的理论分析模式有着严重局限，至少有两个困难：<br />
 <br />
（1）理性人假设。这是现代思想的通用假设，假如不把理性原则看作是唯一最高原则，就不可能得出那些康德式结论。可是理性人假设并不符合人的事实，它仅仅表达了人的心智（mind）,而没有表达人的心事（heart）。这样的分析模式不仅把人切掉了一半，而且很可能切掉了更重要的一半，因为心事才表达了人们真正想要的东西。正因为拒绝了“心事问题”，康德才能够推出一种单调而无矛盾的道德生活，而丰富多彩可能生活就被省略不计了。现代理性主义论证所以显得干脆利索，就是因为省略了许多本来必须计算在内的因素，也就省略了各种本来不得不考虑的困难。回避了心事问题的理性眼界太小，用来理解人和生活恐怕削足适履。而且，仅就理性人假设而言，康德的道德原则也并非唯一的逻辑结论。从理性人假设出发，至少可以同样合理地推出两个以上不同的甚至互相冲突的结论，就像一个方程有两个合法解。理性人不仅可以推论出康德意义上的一视同仁的“道德人”，也同样可以推论出亚当.斯密意义上的追求个人利益最大化的“经济人”。把理性看作是道德基础，既不可靠也不真实，因为理性出产的未必是道德的，理性可以有助于道德，也同样可以有助于不道德。<br />
 <br />
（2）平等原则和个人全权自主原则。康德理论所以对人权理论无比重要，就在于它能够为人权提供合法性论证（justification），特别是证明人权所必须依靠的两个核心假设：平等原则和个人全权自主原则（autonomy，通常译为“自律”，恐怕片面，因为autonomy的核心意义是“自主权”，当然“自主权” 包含“自律”）。尽管康德的“绝对命令”逻辑地蕴涵了平等原则和个人全权自主原则[8]，但康德论证却建立在一些隐秘错误之上。康德论证的出发点是“人皆有理性”，但从这个前提推到绝对命令还需要许多步骤。由“人皆有理性”显然推不出“理性原则是唯一或者最高原则”或者“所有事情由理性说了算”。能够满足“人皆有之”这一标准的人性除了心智（mind），还有心事（heart）、潜意识和本能，每一样都有巨大能量去左右人的选择。理性使人具有自由意志，因此，由“人皆有理性”推出个人“自主权”倒是可行的，但仅仅有个人“自主权”还远不足以支持现代伦理或人权，还必须能够推出平等原则。康德由理性的普遍性品格推出平等，这固然是个有想象力的方法，可惜由理性的普遍性原理只能或然地而不可能必然地推出平等原则，而由理性的普遍性原理同样可以或然地推出许多反平等原则――康德的“绝对命令”的弱点就在于此。例如有人可以满足康德标准而同意让“弱肉强食”、“男尊女卑”或者吸毒、偷窃和贪污成为普遍规范，这说明理性不可能控制人的行为局面。由理性普遍性原理不能必然推出平等原则，这一后果非常严重，它甚至将导致个人自主权原则的崩溃，因为，如果没有平等原则去控制个人自主权的限度，个人自由将在失控中无限扩张，个人主义的利益最大化原则将被无限制滥用，而过分扩张的个人自由必定互相否定每个人的自由。<br />
 <br />
罗尔斯对康德理论进行了重要的补救。罗尔斯虽然没有挑明理性不能必然推出平等的难题，但从他的努力来看，他显然意识到这个可怕的困难，因此想象了“无知之幕”下的博弈，这个虚构条件虽然事实上不可能（许多人批评这一不切实际的设想），但却差一点就成功解决了康德问题。按照罗尔斯的计算，“无知之幕”使得没有一个理性人愿意冒险，于是就“必然地”都愿意接受一个相对平等原则，以保证即使自己碰巧是弱者也能获得相对平等的照顾。罗尔斯虽巧，但仍然没有能够拯救康德理论，因为仍然存在着类似错误。即使以“无知之幕”作为博弈的初始条件，也仍然存在着同样合理的多个理性解而决非唯一解，至少选择“公正”与选择“平等”是同等理性的解。更严重的挑战是，由于博弈总是反复多次的博弈，“无知之幕”下的选择只是第一回合，接下来的无数回合就很快回复到真实博弈。揭开幕帘之后真相大白，许多人就会不满意“无知之幕”下的制度安排，就总会利用制度不可避免的各种漏洞去解构这个制度，甚至利用各种政治手段去重新选择新的制度。这才是理性原则的必然后果：如果说在“无知之幕”下人们的理性选择倾向于选择平等，那么，当“真相大白”，人们的理性选择就必定有所变化，理性总要根据博弈条件的变化去选择最合适的策略。理性为所有事情服务，无论好事坏事，试图让理性只用于某事而不用于别的，尤其是一种幻想。<br />
 <br />
现代哲学家们不断试图为西方人权理论提供哲学论证，但在技术策略上都没有超越康德/罗尔斯水平。例如A.Gewirth，他试图从个人作为“理性的行为者”推出人权的合法性，利用的还是理性的普遍性原则，即在要求自己的权利时就不得不同时把个人权利普遍化[9]。这种论证策略无非重复了康德模式的错误。现代哲学家总是忽视这样的事实：（1）省略掉人性的丰富性，把太多的可能生活忽略不计，这样的理论无法应付各种可能的困难；（2）权利表达的是人们的要求，而人的要求实在太多，世界和生活根本无法支付那么多要求被普遍化的权利，世界和生活会被太多的权利压垮。<br />
 <br />
 <br />
 <br />
 <br />
5．天赋人权的危险逻辑<br />
 <br />
在温和意义上，权利（rights）是对某些自由或利益的正当要求（justified claims）；在强硬意义上，权利则意味着拥有某些自由或利益的正当资格（justified entitlements）。一种“要求”的目的无非是达到一种“资格”，因此权利的最终意义还是资格。无论哪种意义，权利都有着一个基本的反思性问题，即权利的正当性问题（the rightness of rights）。<br />
 <br />
一种资格必须在某个“游戏”中被定义，否则其意义无法确定，因此资格总是有条件的。特定游戏规定了特定资格的限度，对资格的条件限制同时就是使资格成为资格的定义。因此，资格永远具有这样的逻辑结构：<br />
 <br />
&nbsp; &nbsp; p具有做x的资格e，当且仅当，p做某事y；<br />
并且<br />
p具有做x的资格e，当且仅当，p不做某事z。<br />
 <br />
比如说，如果不付钱就不能获得商品；作弊就会被取消比赛或考试资格；犯法就会按法律判刑。在没有成文规则的日常生活中，也存在着自然约定和默认的游戏规则，比如有人品质很差，人们就没有兴趣与他合作，也就实际上把他排除在游戏之外。如此等等。<br />
 <br />
天赋人权理论相信，每人生来就平等地拥有一系列权利，这些权利终身无条件拥有，在任何情况下都不可剥夺并且不可让渡。于是人权似乎是超越任何约定或法定游戏的权利，变成了至上的特权。“人权高于一切”这一逻辑意味着人权高于主权、高于法律、高于制度、高于文化，如此等等。无条件的至上性是非常危险的逻辑，因为无条件的权利是对任何价值标准的否定。<br />
 <br />
“权利为本” （rights-based）的现代性颠覆了“诸善为本”（virtues-based）的自然传统，把“善者优先”的秩序颠倒为“权利优先”，这不是价值观的变化，而是对任何价值釜底抽薪的消解。列奥.斯特劳斯早已发现这个“现代自然权利论的危机”。自然权利（natural rights，即天赋人权）据说源于“自然法”（natural law）的“自然正当性”（natural right），但由自然正当性变成自然权利是一次偷换性的颠覆，因为自然正当性是以“诸善为本”的，这与自然权利以“权利为本”恰好相反，因此，自然权利反而是“自然不正当”。斯特劳斯认为只要是“权利优先”，就必定导致价值虚无主义，因为只要否定了诸善的优先地位就等于取消了所有价值[10]。价值由诸善所定义，如果权利优先于诸善，权利的正当性又能以什么为根据呢？它或者无根据或是任意的根据。这个列奥.斯特劳斯问题提醒我们：当不再以诸善作为依据，就不再有任何正当性的依据了，因此我们必须面对这样一个惊人的事实：一向冠冕堂皇的人权根本没有价值依据。<br />
 <br />
既然超越了善，权利优先原则就必定蕴涵着一个关于权利的悖论：假如对某种自由和利益的要求可以被搞成一种权利，那么任何一种并且所有对自由和利益的要求就都可以按照同样理由被搞成权利，因为，既然权利优先于任何一种善，就不存在任何价值理由去规定哪些要求能或不能被搞成权利。这个悖论将是价值混乱和社会失控的根源，而且已经开始表现在人权的实际发展状况中。通常认为到现在已经发展出三代人权：第一代是政治权利和公民权利；第二代是社会和经济发展权利；第三代是各种文化和不同价值观的权利。目前权利种类已经很多，而且越来越多，权利终将过满为患。权利背后是欲望，欲望无数而且互相冲突，因此权利也互相冲突，而没有一个世界能够支付其多无比的权利。权利反噬权利是个无法避免的问题，例如第二代人权会削弱或损害第一代人权，而第一代人权则会损害第二和第三代人权，甚至第二代人权也会损害第三代人权。甚至在西方认为比较保险的第一代人权之中也存在许多互相冲突，而且第一代人权的项目就已经发展得太多以至于社会难以承当[11]。为什么会出现人权的膨胀和失控？究其原因，人权的注册条件太低，几乎就是无条件注册，因此随便什么自由和利益要求都可以被搞成人权，而且各种批评都被认为是“政治不正确”。<br />
 <br />
问题还远不止是世界支付不起太多的权利。人权不仅注册条件太低，而且还承诺太高，它承诺了永不剥夺的权利，承诺了成本惊人的权利，这又将导致社会游戏的崩溃。想象一个游戏，如果无论怎么耍赖都不用出局，这个不公正的游戏肯定是可疑的。考虑人权的游戏情况：给定任意一个人无论做什么事情都永远保有不可剥夺的人权，于是，无条件的人权蕴涵着“破坏他人人权的人拥有人权”。根据“破坏他人人权的人拥有人权”这一逻辑，如果某人为了私利去破坏他人的人权，他就等于获得额外奖励，即“为自己利益去破坏他人人权而无损于自己人权”这一奖励。这样不正当的奖励不仅破坏了公正，而且破坏了平等，破坏了人们对善恶是非的正常理解，特别是破坏了人类正常生活所需的博弈环境和博弈条件，因为它在逻辑上蕴涵着：（1）社会的博弈环境相对有利于坏人；（2）人权制度相对有利于破坏他人人权的人；（3）人权社会相对有利于不公正的行为。诸如此类。显然，只要损害公正原则所要求的行为与结果的对称关系（善有善报，恶有恶报），做坏事被惩罚的风险减低，而且惩罚也相当轻微，总之，做坏事的成本变小而收益很大，通过坏事而获利就变成优选策略。天赋人权所以是危险的，就在于它是一条反公正原则。人权所追求的平等、尊重生命、个人自由等等都是可取的，但必须以公正为前提才是可能的，一旦公正原则崩溃，所有其它价值也将如覆巢之卵。<br />
 <br />
任何社会都必须以公正原则作为唯一最高原则，否则必定导致价值混乱和社会失控。按照中国哲学理论，乱世会破坏所有好事情，治世虽不能保证所有好事情，但至少有利于某些好事情。任何一种游戏，无论多么简单，都必以公正原则作为游戏的元定理，否则无法进行，即使如棋牌球等娱乐，如果允许作弊、耍赖或违规，游戏马上崩溃。法律更是如此，公正原则是任何法律的正当性和有效性的依据。德沃金指出法律必须以公正原则作为“立法意图”，而立法意图表现在法律的各种元定理中，例如“任何人都不得从其错误行为中获得利益”（参见德沃金对“埃尔默案件”的深入分析）[12]。现在这个流行人权文化的社会所以还没有崩溃，是因为法律、政治和经济还没有完全为人权所统治，还有许多在现代得以幸存的传统观念，因此社会游戏得以维持。<br />
 <br />
可以考虑关于死刑的问题。人权理论认为，死刑是个法律错误，尽管目前法律仍然“有权”处死罪不可赦的罪犯，但从理论上说这是“错上加错”――无非都是杀人。其实死刑确实可以取消，死刑只是极端惩罚的一种形式而已，真正的问题是，我们仍然必须有某种可怕的极端惩罚能够使得犯罪成为得不偿失的错误行为，否则犯罪就会成为优选策略。而按照人权理论的想象，不仅死刑等极端惩罚应该取消，一般的法律惩罚也都应该大大削弱。假如全盘按照人权来制订法律的话，社会必定是坏的，因为如果取消“罪与罚”的对等性就破坏了社会公正，在社会中，坏人坏事就都获得博弈优势，结果必定是扶持坏人去破坏更多人的人权。如果极其残酷的犯罪只得到轻微惩罚，又如何告慰受害人以及受害人亲属？甚至有人认为不仅应该取消死刑，而且因为监狱生活不好而应该减刑，却没有考虑到被杀害的人连生活都没有了，而且受害人的亲人可能一生痛苦。要把貌似多情的无情说成是“进步”和避免“错上加错”恐怕是困难的。而且，现代人权理论连同现代法律都是物质主义的，只看重生命和财产，在很大程度上忽视了命运、心理、情感和精神，因此在衡量对人的伤害上有大量失误，比如残害妇女儿童、拐卖儿童、毁容、制造假药以及有毒食品等等，这些都可能毁掉受害人一生的命运和幸福，而由于没有“杀人”，罪犯往往只得到相对轻微的惩罚，就好象只有“生命”才是重要的，而“一生”的痛苦却不值得计较。关心罪犯的痛苦超过关心受害人的痛苦是一种令人震惊的罪行，在此背后很可能有一种虚伪的宽恕和仁慈的自我表现。可以设想一个“换位检验”：假如一个人自己或者他的至爱之人成为残酷罪行的受害人，他仍然同意人们重视罪犯的痛苦远远超过重视他作为受害人的痛苦，那么他才是真正的人权支持者。即使有人能够通得过这个“换位检验”，我们也有理由怀疑这种不正常情况的普遍性。<br />
 <br />
总之，破坏公正原则最后必定导致社会价值崩溃和人心失衡。虽然我们不怀疑天赋人权理论的良好动机，但有理由认为它考虑不周，缺乏理论上的谨慎。<br />
 <br />
