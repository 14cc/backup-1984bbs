 <br />
6．普遍人权的元理论问题<br />
一种人权观念可以是一个因时因地的政治策略，但如果要成为普遍的价值观念就必须能够通过理论合法性的检查，就是说，任何一种人权理论都需要元理论的支持。为了能够重新思考人权理论的设计，特别需要反思以下几个基础性问：<br />
 <br />
（1）&nbsp; &nbsp; 人权的存在论基础<br />
 <br />
如果没有他人，就根本不存在人权问题。所以需要人权，就是因为需要处理“我与他人”的关系，因此，“人际关系”，而不是“个人”，才是人权的存在论前提，人权问题必须落实在人际关系上去分析。我们准备选择这样的存在论：关系是存在论基本单位，并且，关系先于个体，人的所有生活问题都必须在这一分析框架中去理解。这与西方以“个体”为存在论基本单位的分析框架完全不同。<br />
“个体”（individual）意味着“不可再分的单位”，它如果用来指示事物，应该是一个合适的存在论标志，但如果用来指示人，则不能正确表达人的存在性质，而且是对人的非法删节。Individual更适合表达人的身体性存在，却不能表达人的精神性存在。例如，日常语言中可以说到“我的身体”和“我的情感”，但其逻辑语义却完全不同，“我的情感”必须是“及物的”才有意义，在大多数情况下，它的及物性表现为“涉及他人”。这意味着，“我的情感”并不是一个限制在individual之内的事实，而是一个属于人际互动空间的关系性事实。孔子对人有更深入的理解，孔子用仁（二人）来解释人所以为人，其深意就是要在“关系” 中去理解人。把人的概念转换为个人概念去分析是一种“存在论偷换”。<br />
 <br />
人的存在所以形成最深刻的存在论问题，就在于它突破了普通存在论的一般形式。普通存在论是以物理存在（the physical）作为基本存在形式的，所以存在论就成为“物理学之后”（meta-physical）的研究。西方存在论的这一传统定位是存在论的错误方向。人的存在方式是有意义地“生活”而不是生物学的“活着”，人的存在场域远远溢出在身体之外，人是在与他人的相互关系中被定义的，因此说，关系先于个人，关系之外无个人，关系为实，个人为虚。在关系中，他人始终是优先的，因为他人总是一个多数集合，远大于“我”，而且是“我”的存在环境和条件，相对于“我”，他人总是无限大。他人的存在论优先地位决定了“我”的所有权利都永远是他人的恩赐。“我”并没有因为自由意志就成为所谓的主体，自由意志仅仅表达了“我愿意如此这般”，却不能保证“我可以如此这般”，因为他人不见得允许我如此这般。西方存在论在把“人”偷换成“个人”之后，又进行了二次存在论偷换，把“个人”偷换成“主体”，因此制造了个人自由和个人权利至上地位的幻觉。主体/个人是人造的虚拟存在，它误导了生活。如果要正确使用主体这个概念，就必须理解到“主体总要从属于他人”（to be a subject is to be subject to the others），也就是首先承认“他人”的核心地位。这是以孔子为代表的中国哲学也是列维纳斯哲学的原理[13]。<br />
 <br />
以关系存在论为基础，可以看出，权利必须在相互关系中去理解。显然，如果没有他人，或者说不存在某种关系，就无所谓权利。如果与任何他人都没有任何关系，比如鲁宾逊，却要说“我有不被干涉的权利”，这是荒谬而无聊的。因此，人权在本质上要表达的不是个人自由，而是人际关系对个人自由的正当限制，就是说，不可能先界定个人权利而后界定相互责任，而必须先界定相互责任而后才有可能界定个人权利。任何一种权利都存在于“关系”中，而不是事先存在于个人“身上”，这决定了“由责任决定权利”的存在论顺序。这个存在论顺序不可以颠倒，否则后果很严重。<br />
西方权利理论错误地把存在论的基本单位选定为“个人/主体”，这样就把权利看作是个人存在的一个自然属性。可是显然没有任何证据能够证明个人身上具有这样一个自然属性，甚至从“个人”概念也不能分析地蕴涵权利，即权利无法由“个人”必然推出。假如一定要强行从“个人”推出权利，则要冒很大风险。“个人”身上自然就有的只是欲望、需要和自我中心意识，假如把个人所欲的某些东西说成是对权利的“合法要求”，那么就同样可以把个人所欲的所有东西都说成是对权利的合法要求――因为在“个人”这个分析单位中找不出拒绝把某些欲望变成权利的限制性理由。把“个人”当作权利的分析单位所以是个严重错误，就在于“个人”的存在本身并不包含任何限制性理由。人什么都想要，于是什么都可以被宣称为权利，欲望的膨胀导致权利的膨胀，最后，权利会把生活空间挤爆。人们宣称太多的权利，一种权利就变成了对别的权利的破坏。现代人过度迷恋权利，以至于忽视权利无限扩张所导致的社会困难。不断扩张的私人空间必定侵犯别人的私人空间，甚至侵犯公共空间，从而形成权利反对权利的局面。“我”的权利意味着他人的责任，权利太容易被“宣称”，而责任很难落实，实的跟不上虚的，有限责任能力无法支付无限扩张的权利。<br />
 <br />
因此，我们有理由修正人权的存在论基础，把“关系”看作是权利的存在形式，于是，权利的合法性不再落实在个人身上，而是落实在关系中。权利是他人所承认的责任的对应形式，如果没有他人的承认，权利就没有合法性。由于他人总是试图避免责任，于是，权利总是博弈的结果，而正当的权利就是公正博弈的结果。以上我们证明了，由于“个人”由其本身无法证明他所宣称的权利的正当性，因此只能在“关系”中去定位权利的正当性。<br />
 <br />
 <br />
（2）人的概念<br />
 <br />
人的概念支配着人权的意义，因此必须选择一个能够最充分表达人性的概念。<br />
既然西方强调“天赋的”人权，所默认的人的概念就只能是生理学意义上的人，只要生理上是人，就拥有无条件的人权。把人的自然属性说成人权的理由，这意味着权利只与“是”（is）有关而与“行为”（do）无关。用自然身份兑换社会权利，这无论如何是相当奇怪的，因为自然界不存在权利这件事情，权利是社会游戏的一个因素。假如自然身份与社会权利之间可以有跨界兑换关系，权利的应用领域就会被扩大到失控的地步。许多主张动物权利的人，如T.Regan和P.Singer等，就看准了生理人概念界限不清的问题，因此宣称权利应该扩展到许多高级动物上[14]，因为它们也有相当的智力、感受和意识水平，而在动物和人的意识水平之间并没有清楚的界线（令人尴尬的是，许多高级动物与人的智力差距还不及人之间的智力差距那么大）。甚至在Singer看来，“人”这个概念没有太大意义，意识水平才是定义高级存在的标准。有趣的是，动物权利虽是个混乱的问题，但它却是从人权推出的结果，可见人权概念是个混乱之源。<br />
 <br />
人的概念只有能够表达出人的独特价值才是有意义的，只有道德才能表明人的行为的特殊意义，才能表达属于人的独特生活问题。显然，人的概念只能是“道德人”而不是“生理人”。建立在生理概念上的人权理论不仅缺乏道德意义甚至破坏道德，最终将破坏人的概念。生理人只能表明人的自然行为，却不可能表达社会行为，以自然行为而要求获得人权这样的社会报酬，显然不合逻辑。如果一定坚持这样的逻辑，混乱和困难还在后头，不仅是动物，将来还会有生物学创造的各种怪人，还有特别像人的机器人，诸如此类。显然，对于人权理论，有效的人的概念只能是道德人。<br />
 <br />
既然由自然属性推不出道德，那么，在人权问题上，“人”不构成理由，“做人”才是个有效变数。按照我的“可能生活”理论，对于人来说，一般存在论的“存在”（to be）是一个错误的分析形式，正确的形式应该是“因义而在”（to be meant to be）这一扩充形式[15]，这一改革的基本理由是，对于人来说，一个人是人，这其中“是”（to be）的意义无法由“是”来表现，或者说，存在本身（to be）不是存在的一个值（value），而必须在“做”（to do）中去实现，“做”成为“是”的意义明证（evidence），因此，存在无非做事（to be is to do）。这一存在论的新公式表明了人的存在因其化成行为而获得意义，因此，德性是定义“人”的概念所必需的条件。对于德性（virtues），可以有这样的客观判断指标：如果无论你是否具有品质v，你都愿意与具有v的人进行合作，或者，如果别人具有v，那么你也愿意自己具有v，那么v就是一种值得追求的德性。正因为人有德性，因此人这个概念才具有识别特征。<br />
 <br />
把人的概念标准降低到生物学指标，这不是博爱，而是对人的行为价值的彻底贬值，是在否定人的德行和高尚努力。假如人们不再需要追求高尚品质，就能永远无偿地享受所有权利，人类的优秀品德和道德行为就一钱不值了，其荒谬和危险性就像不管学习好坏人人都得优，或者无论是否劳动人人都得同样报酬。如果社会如此不公，人们迟早会发现最佳策略就是去做自私无耻的人。人本身不是目的，但人必须有人的目的。人是做成的，而不是本然的（a man does rather than is），在自然上“是”一个人不等于在道德上“做成”一个人。选择“道德人”作为人的概念，意义在于，只有把人的概念与美德联系起来，与人类社会所需要的优秀价值联系起来，才能够使人的概念具有分量。缺德的人在生理上与道德人是同类，但在伦理上却是异类。如果抹杀这一基本差别，把人的概念落实在人的自然存在上，通过这样抹平价值去达到的只能是劣平等，这种向低看齐的现代主义平等决不是一个好社会的理念。只有以道德人概念为基础才能形成向高看齐的优平等。<br />
 <br />
（4）&nbsp; &nbsp; 公正原则的优先性<br />
 <br />
假如给定一个游戏的意图是非合作博弈，那么它的基本假设就将包括理性原则，个人利益最大化以及风险规避。但如果给定一个以合作为意图的游戏，它的基本配置就将是理性原则，公正原则，美德原则以及共同利益最大化（合作比不合作所需的条件组合要复杂得多）。游戏意图还决定了游戏的“形势”：假如意图是非合作，那么“个人”在该游戏中占有优先地位，人们将先考虑个人自由最大化，然后再考虑不得已的限制，也就是以“个人”为主导去规定“关系”；假如意图是合作，那么“关系”就占有优先地位，人们将先考虑最好的可能关系，然后再考虑个人能够保留的自由，即以“关系”为主导去规定“个人”。人们总是希望生活游戏能够同时兼备公正、自由和平等这些性质，但对公正、自由和平等的重要性排序存在不同偏好，因此我们需要从技术上去分析什么样的排序能够保证所有价值都得到满足。<br />
 <br />
现在来分析人权游戏。西方承认的人权体系主要强调个人生命、私有财产和个人自由（特别是政治自由）。以个人为本的权利体系的第一价值是自由，其次是平等。自由和平等不仅压倒了公正，而且修改了公正的本义，现代理论往往以自由和平等去解释公正，结果挤掉了公正的本来意义，把公正变成自由和平等的一种组合方式，这样就实际上取消了公正，从而造成许多自毁性隐患。权利意味着个人的自由主权空间。个人自由空间的边界在哪里？这是个问题。既然公正不被看作是最高判断原则，权利界定就没有普遍标准，这使得主体间永远是个是非之地，就像国际间永远是个是非之地。<br />
 <br />
从个人出发去解决个人边疆问题的现代方法是理性谈判，哈贝马斯就寄希望于商谈理性能够克服个人理性的缺点，他相信通过理性对话最后总能够达成互相理解从而形成一致意见。哈贝马斯虽然看到了理性互动能够最大限度地发挥理性的潜力，但他忽视了“互相接受”这个必要环节，于是，“对话，理解，接受，一致意见”这个必要流程被简化为“对话，理解，一致意见”，问题是，从理解推不出一致意见[16]。如果不能解决“接受”问题，哈贝马斯方案所能达到的最大限度的理性成就至多相当于程序公正（形式公正），而不可能达到实质公正，也就无法解决任何实质性问题，比如说，根据什么标准来规定价值以及价值排序的问题，或者，根据什么标准来选择人权项目以及这些项目排序的问题。由于形式公正无法保证实质公正，因此公正一直是个没有完成的问题，也就不能解决权利的正当性问题。显然，如果坚持自由和平等的优先地位，就等于否定了公正，也就很难形成合作，而一个缺乏公正合作的游戏将反过来损害自由和平等。<br />
 <br />
如果要把自由、平等和公正这三种众望所归的价值结合起来，唯一可能的排序是公正、自由、平等。这几种价值的不同性质注定了它们不同的弹性：自由和平等都有比较大的弹性，可以多一些或少一些，而公正几乎没有弹性，只有“公正或者不公正”，不存在比较级。于是，只要稍微削弱自由和平等就能够与公正兼容，而如果反过来，则必定破坏公正。因此，从技术性上说，“公正优先”模式是唯一能够同时保证公正、自由和平等的兼容排序。<br />
 <br />
公正原则的完美程度与真理相似，而且在结构上也相似。真理就是把如此这般说成如此这般；同样，公正就是对如此这般的付出给予如此这般的回报，同样都是对称或等值关系。如果不以真理作为知识标准，知识就崩溃；如果不以公正作为游戏标准，游戏就崩溃。所以说，公正是任何权利获得普遍有效性的唯一条件，也是权利获得合法性的唯一根据。如果失去公正，就必定有些人宁愿不合作而导致游戏崩溃，或者不接受而退出游戏。显然只有公正原则才能定义一种不包含自毁因素的权利游戏。公正原则的完美性和力量在于它的对称性或等值性，它使得任何反对意见都没有立足之地。所谓人权，就是公正关系所允许的个人自由空间，而不是个人所要求的自由空间。也许有必要再次强调，我们所使用的公正指的是古典含义的公正，即行为与报应的对称或者付出与回报的对称，其结构相当于逻辑上的互蕴关系（p iff q）。<br />
 <br />
 <br />
7．作为新普遍主义的预付人权理论<br />
 <br />
根据以上的基础分析，我准备推荐的预付人权理论实际上已经水到渠成。为了更好说明问题，我们也可以设想一个原始博弈，不过这个博弈不需要霍布斯“丛林”，也不需要罗尔斯的“无知之幕”，也不需要经济学家们喜欢的“公共财产悲剧”。这些初始条件都太做作，与生活真实相去太远。尽管理论设想的初始条件总与真实世界有些出入（这是允许的），但如果初始条件过于单调，就恐怕与真实世界无法匹配。由一个与真实相差太远的游戏推导和总结出来的规律未必能够代表真实生活的规律，而把幻想出来的规律应用于真实生活恐怕是危险的。于是，我们有理由要求一组与真实世界虽然有些不同，但与真实世界比较匹配的初始条件，即一组尽量仿真的条件。大概如下：<br />
 <br />
（1）&nbsp; &nbsp; 每个博弈方都优先考虑自己的利益，包括自己的专属利益和自己可及的共享利益，并且，在专属利益与共享利益之间不存在先验给定的偏好排序，比如不存在“专属利益优先于共享利益”的排序，而仅仅考虑某种利益，无论是专属的或是共享的，是否是自己可及的最大利益；<br />
（2）&nbsp; &nbsp; 每个博弈方的思维是理性的，但思维能力不等，因此各自的策略水平不同；<br />
（3）&nbsp; &nbsp; 每个博弈方将按照各自的价值偏好排序表去理性地计算得失，而不存在一个普遍通用的价值排序表。假定p偏好x，即使其他人都认为x一钱不值，p仍然为了x而牺牲别的利益，这一计算将被认为是充分理性的；<br />
（5）&nbsp; &nbsp; 足够多次的连续博弈，类似于历史的效果；<br />
（6）&nbsp; &nbsp; 每个博弈方拥有关于其它博弈方的部分知识；<br />
（7）&nbsp; &nbsp; 每个博弈方各自拥有的初始策略知识不等，但可能的策略是有限多个的，而且每个博弈方都能够学会其他博弈方的策略。<br />
 <br />
根据这一仿真社会的初始博弈条件，可以获得以下分析：<br />
 <br />
首先我们可以修正一个流行的错误。在通常的分析模式中，个人利益的最大化仅仅计算到自己的专属利益，而没有把对自己同样有利甚至更有利的共享利益计算在内，因此才会把理性人定义为互相麻木不仁的人。事实上，人的大多数“最大的”利益都只存在于共享关系中，可以表达为：对于某人，存在着某种最大利益x，当且仅当，x同时为他人所分享。就是说，x仅仅存在于与人共有的关系中，而不可能为个人所独占。例如家庭、爱情、友谊以及任何合作所创造的巨大效益。人们真正关心的利益是“自己可及的利益”，而不是个人独占的利益。人们对利益的理性排序完全不像现代理论所妄想的那样，永远把政治自由和财富排在最前面，因为人们的最大利益往往属于由“关系”所创造的利益，比如安全、幸福、成就和权力。强调理性计算，本身并没有错，但现代理论把需要计算的项目搞错了，被漏掉的利益项目太多，尤其把最大利益漏掉了。当纠正了在利益项目上的计算错误，就能够发现人们的博弈真相：人们所以苦苦进行博弈，根本上不是为了获得一些宣称拥有个人自由空间的消极权利（据说是最基本的人权），而是为了形成最好的制度，这个制度保护了能够使人们获得最大利益的所有合作关系，而合作关系是安全、幸福以及各种最大利益的必要条件甚至是充分条件。这个至今尚未存在的最好制度的标志是：（1）所有人都一致承认这个制度；（2）所有人都失去采取不合作行为的积极性；（3）所有人都有自由选择的机会去形成个人幸福的帕累托改进。<br />
 <br />
博弈的第一回合甚至许多个回合都不足以形成长期稳定的制度和规则，大家在别人出牌时互相了解对方情况并且互相学习到各种策略，这意味着后续博弈条件和博弈策略不断被改进，能力更强的人不断推出更高明的策略使自己利益占优，但领先总是暂时的，高明的策略很快变成公开的知识。一定要等到“集体黔驴技穷”，大家拥有足够饱和的共同知识或对称知识（对称的知己知彼）以及普遍知识，这时将出现普遍的策略模仿，大家都模仿某个被证明为最好的策略，于是达到均衡和一致，成功的制度才能够产生。<br />
 <br />
在足够多回合的博弈之后，最有可能被普遍模仿的策略将是对称性公正。可以这样证明：给定人人都是理性的，按照博弈论，理性计算的一个标准是：不吃亏并且至少不比别人更吃亏（风险规避），那么，假如任何一个“冲突”策略被普遍模仿，必定所有人都吃亏（霍布斯丛林定理）；假如任何一个只顾自己、漠视他人的“不合作”策略被普遍模仿，大家都只能得到比较失望的结果（纳什均衡）；假如一个罗尔斯式的“合作”策略被普遍模仿，表面上似乎能够有比较好的结果，但却是一个不可能达成稳定均衡的策略，因为只要允许以某种理由去形成某种偏离公正的福利特权，就会有无数种偏心的理由都来要求福利特权。所有价值观就会卷入争夺霸权的冲突，因此将回复到不合作状态；最后，唯一能够避免所有偏心理由的“合作”策略就是对称性公正。对称性公正的策略越被普遍模仿，制度就越稳定，冲突就越来越少，这一点与自由和平等的策略形成强烈对比，自由和平等策略越被普遍模仿，冲突就越多。博弈论有个未决的重要难题：不合作如何才能够形成合作？这个问题所以一直不能解决，恐怕就在于没有充分考虑公正策略。自由和平等的优先不能带来公正，相反，只有公正优先才能够定义所有人都可以接受的有限自由和有限平等，或者说，无限公正才能规定并保护有限自由和有限平等。<br />
 <br />
根据以上的博弈分析，一个具有普遍必然性的人权制度只能以公正原则作为唯一最高原则去定义的人权体系，人权就是每个人能够被公正对待的权利。又根据关于人的概念的存在论分析，一个人之“所是”（is）还没有完成人的概念，一个人必须在其“所为”（does）中完成人的定义，正当的做人方式是一个人拥有人权的资格认证。考虑到人的概念的双重性和过程性（由生理人到道德人），能够充分全面地表达公正原则的人权概念就只能是预付人权，而不能是天赋人权。天赋与人的仅仅是生命和能力。人类文明把人权预付给人，就是期待他做成一个合乎道德要求的人，一个人必须“做”成一个道德的人，才“是”一个完整意义上的人，才能保有人权。人不能只享受人权而无视做人的义务。因此，预付人权的基本原则是：<br />
 <br />
 <br />
（1）&nbsp; &nbsp; 由于做人需要一个过程，人权这种资格就只能事先给予并且事后验证，所以人权是预付的。任意一个存在，只要是人，都无例外地得到预付的任何一项人权，或者说，每个人生来就获得人类借贷给他的与任何他人相同的权利。<br />
 <br />
（2）&nbsp; &nbsp; 人权虽然不劳而授，但决非不劳而享，否则损害公正。因此，预付人权是有偿的，是有条件保有的。所有人权，包括生命权和自由权等，都是有偿的。一个人获得预付人权就意味着承诺了做人的责任，并且将以完成做人的责任来偿还所借贷的权利。一个人可以自由选择是否履行做人的义务，如果选择履行做人的义务，则一直享有人类游戏的全部权利；如果拒绝履行做人的义务，则视同自愿退出人类游戏，准确地说，如果拒绝了预付人权所要求的部分或全部义务，就视同自动放弃了部分或全部人权。<br />
 <br />
（3）&nbsp; &nbsp; 根据“理性知识永远有限”的原理[17]，任何规划出来的人权体系都只能被认为是历史性的或暂时性的，永远都存在改进甚至改写的余地，因此，一个人权体系将给予每个人哪些权利以及什么限度的权利，这要取决于世界在特定时代条件下的支付能力，随便宣布太多有名无实的权利除了增加社会冲突和搞乱世界，并无积极意义。任何一个人权体系的根本问题不在于它许诺了哪些权利和多少权利，而在于它所许诺的权利是否具有正当性及其证明（legitimacy and justification）。任何一个人权体系都只能以公正原则作为唯一普遍有效的解释原则（元定理），因为除了公正原则并不存在任何其它原理能够证明正当性。任何一个人权体系以及它所包含的每一种人权都必须具体地落实公正的对称性关系，都必须是公正理念的具体范例，这样才具有正当性，而任何偏离公正关系的权利都是不正当的。<br />
 <br />
（4）&nbsp; &nbsp; 形式公正不能保证实质公正，这是公正的最根本难题。要确定具体内容上的对称关系确实存在着技术上的困难，因为几乎不存在能够证明两种不同的东西是“等值的”客观标准。最好的主观标准是所有人的一致同意，但这一点几乎做不到。一般的解决方式是以民主去替代一致同意，但以多数否定少数本身就是不公正，而且还可能导致更坏的事情。也许比较好的方法是想象一个最少当事人模型：如果双方一致同意pRq是一个公正关系，并且，双方一致同意角色互换的qRp同样是公正关系，因此（pRq）=（qRp）的换位等值关系成立。任何持有不同意见的其他人可以作为任意变元代入为这个模式中的模拟“当事人”去接受检验，这样可以排除作为旁观者的偏见。于是获得这样的理性解：如果任何人代入当事人而不发生不同意见，则这个最少当事人模型就象征性地反映“所有人一致同意”；如果有人代入当事人之后仍然有不同意见，则视同自愿退出由公正原则定义的权利游戏，也就视同自愿放弃受保护的权利。据此很容易发现天赋人权理论是不公正的：显然不可能所有当事人一致同意破坏他人人权的罪犯以无条件人权为名而逃避相应的惩罚。<br />
 <br />
（5）&nbsp; &nbsp; 如果说权利是资格，那么义务就是代价或者成本。权利和义务关系的公正同样在于对称性，即权利和义务是互相蕴涵的：某人p拥有某种权利R，当且仅当，R承诺了与之对称的义务O。如果某人拥有的权利大于义务，就等于把部分义务推卸到别人身上，或者等于多占了别人的利益，因此，在某人p所承诺的权利/义务关系与任一他人q所承诺的权利/义务关系之间同样存在着互相蕴涵的关系：p（R&#8596;O）&#8596; q（R&#8596;O）。而天赋人权关于权利和义务关系的理解有逻辑错误，由于认定人权是无条件的，因此，以上的两种互相蕴涵关系就被简化为一种互相蕴涵关系：p的权利蕴涵q尊重p的权利的义务，反之亦然，即（pR→qO）&#8596;（qR→pO）。这是以平等冒充公正的典型模式，它隐瞒了权利和义务的合法性问题。假如给定一个游戏，人人无论怎么耍赖作弊，都不会被取消游戏资格，这样的游戏虽然兼备了自由、平等和公平（fair），可就是没有公正。可以想象，这个游戏是玩不下去的。所以必须强调对称性公正，就在于我们不能随便替他人做主，不能随便就把他人心灵给代表了，他人未必同意我们拥有如此这般的权利以及因此强加给他的义务，他人想象的权利可能有所不同，或者权利排序的偏好有所相同。只有先承诺我的义务以获得我的权利的正当性，然后才有正当理由去申请他人尊重我的权利的义务。义务和权利在逻辑上是“同时的”，但在价值上义务先于权利，因为只有义务才能够保证权利的正当性，而反之不然。于是又有这样的关系：p所承诺的义务pO在先,并且蕴涵着相应的权利pR，因此又蕴涵着他人q尊重这一权利的义务qO，即（pO → pR）→ qO。这种解释的优点是能够避免在人权问题上各种不公正的实践难题，比如“破坏人权的人享有人权”这类难题。一个人如果破坏人权就是拒绝了义务pO，由于pO蕴涵pR，拒绝pO就失去pR，也就不再有理由要求他人的义务qO。在实际生活中如果他人愿意继续承当义务qO，那是因为宽容的美德，而不是必须。如果不意识到这一点，就是对美德不公。<br />
 <br />
 <br />
&nbsp; &nbsp; 德沃金有名言“个人权利是个人手上的政治护身符”（或译为“政治王牌”，trump）[18]。这多少点破了个人权利或天赋人权的政治实质，它是个人用来反对政府或集体的一个反抗理论，现在又进一步成为各怀目的的各种非政府组织的反抗理论，也成为西方用来鼓动非西方的民间力量的反抗理论。这一反抗理论在以弱抗强方面当然有其积极意义，但却不是一个适合于以公正和合作为标志的成熟社会的权利理论，而且它在理论上缺乏谨慎的技术性考虑，从而暗含着权利反对权利的自毁逻辑，因此更不是一个成熟的普遍有效的权利理论。我相信预付人权是一个比天赋人权更具思想合理性的权利理论，它保留了天赋人权理论的几乎所有优点，而消除了天赋人权理论反公正的危险因素，不仅具有理论的普遍有效性，而且具有允许因地制宜的实践弹性。<br />
