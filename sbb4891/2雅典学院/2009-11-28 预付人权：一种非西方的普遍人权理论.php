													<h2>人权只有一代</h2>
						
						
																											<div id="postmessage_347444" class="t_msgfont"><a href="http://www.zjol.com.cn" target="_blank">www.zjol.com.cn</a> 2006年11月24日 浙江在线新闻网站 <br />
<br />
　　推荐阅读：&nbsp;&nbsp;<br />
<br />
人生识字糊涂始，教育由启蒙沦为蒙蔽之时，往往是因为文字里的昏话太多。在中国这样一个许多人读不起书的国家，教育资源相当稀缺，可惜的是，不少人有幸得到这种资源，读成了知识分子和大学校长，反而只把取巧工媚放在第一位，有意忽视本来很简单的常识。批判与启蒙的鲁迅“奶牛”，“吃的是草，挤出的是奶”；而鲁迅之后越来越多的“二奶牛”，喝的是奶，挤出来的是草。 <br />
<br />
我今天一觉醒来，发现人权已经到了第四代。为了避免自己更新不快失掉人权(因为在许多人权专家的眼里，知识不够多是没资格有人权的)，赶快补了一下课：《新京报》11月23日报道，中国政法大学校长徐显明表示，以和谐精神超越传统三代人权的精神，将化育出新一代人权―――和谐权。 <br />
<br />
和谐与人权都是相当好的词语，能把它们合乎逻辑地拧在一起变成“和谐权”，当然是了不得的“学术成果”，给专款、搞专题似乎都是应该的。但是在短短的时间里，已经有太多人滥用、曲解“和谐”从而将自己的“不和谐”合法化、神圣化。一个城市的主政者粗暴对待主张自己基本权利的市民，可以说是为了和谐；禁止不同意见甚至是善意批评，甚至把流浪汉、小贩们打倒在地，都能说成为了和谐…… <br />
<br />
在这种风气中，徐显明校长的结论我就不敢轻易接受，在我看来，一个人生来就有的权利，即所谓的“人权”，可能再过一万年也不会变，人权只有一代，不像游戏机，隔几年就推一代。说得朴素一点，一个人生下来，成年了，他就有看到一切的权利、听到一切的权利、有用双脚行走到任何地方的权利、有评论一切发表任何观点的权利、有决定自己行为方式与生活方式的权利、有快乐生活反对任何恐吓力量的权利。这些都做到了，就是完整的先进的人权，做不到，就是人权不够。 <br />
<br />
徐校长先自行将人权划分为四个阶段，前三个阶段分别是自由权本位的人权，生存权本位的人权和发展权本位的人权。不管他的这种划分法是否科学，也不费事逐条讨论他的论证过程，好在尝一口就知道梨坏了，化繁就简，只说一条吧。徐校长对人权的“第一阶段”有这样的论述：“自由权本位的人权忽略了人与人事实上的不平等，不平等的人权使人权体系难以和谐”。人是生而不平等，比如出生于中国的偏僻乡村、印度的低种姓家庭及世界上一切国家的贫穷地区，人生已经输在射精阶段了。而这种弱势阶层的自由权是最容易被剥夺的，从而固化、加剧不平等，比如不许农村人口自由迁徙进入城市。 <br />
<br />
所谓的不平等就是把一部分人当人，把一部分人不当人；一部分人有自由，一部分人没有自由。后者争取自由权的过程就是人权的进步史，自由权从来都是消除不平等、构造和谐的最重要工具。在人权史上，自由权一直只有不够，而没有过多，而在人权史上最严重的退步一般都发生在人权专家们认为自由过了头。 <br />
<br />
徐校长太心急了，太急了，心急吃不了热豆腐，也速成不了当红人权专家。 (作者系专栏作家) <br />
<br />
来源： 南方报业 　作者： 连岳 　编辑： 陈国平</div>

							
							
							
							
