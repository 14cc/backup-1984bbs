													<h2>中共政权即将崩溃的7个特征</h2>
						
						
																											<div id="postmessage_848525" class="t_msgfont">1.国家财政支出，包括中央财政和地方财政支出，急剧增加，但各级财政收入却难以增加，或者是在人民的强烈不满中增加的。吃皇粮，吃财政饭的人越多，不吃财政饭的人民的负担就越大。因为人民的负担越大，所以，人们就更加想变为吃财政饭的人。这样就形成了一个恶性循环。结果是，财政开支迅速增大。人民的负担和抗议越来越大。中共的现代财政与中国古代有些不同。中共最近几十年，靠挖矿卖矿来钱和卖地来钱，维持财政收入的快速增加，以维持财政开支的快速增加的需要。但是，土地是有限的，而且，低价征地，高价卖出，会得罪原来的土地使用者。地下的矿产尤其是富矿是有限的，中共的挖矿成本迅速上升，有些矿产迅速挖完。所以，中共的财政收入将来难以维持那么大的水平，除非，向老百姓和企业界开征高税率。但是，向人民开征高税会激起人民的强烈反抗。所以，中共政权会因为财政危机而崩溃。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 2.中共政权是靠军警的迅速增加来维持的。警察的快速增加，说明这个政权统治下的人民，很难对付了，只能靠快速增加警察来对付。在中共目前的情况下，军队是可以充当警察的功能来镇压人民的。所以说，军警的快速增加和军警的开支快速增加，可以说明，中共的政权不稳了，快完了。<br />
&nbsp; &nbsp; <br />
<br />
&nbsp; &nbsp; 3.当一个政权大搞特务政治时，这个政权就快完了。中共的特务政治遍天下！全国成年人口中，大约每100人有10-20人是中共特务线民。搞特务政治，最能激起人民对政府的仇恨和反抗。因为，政权不仅不能给人民带来安全感，反而用特务政治给人民带来不安全感！<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp; 4.一个国家的人才大量流失到国外，或者大量流失到民间。人才不愿留在一国政权里，或不能进入一个政权，或即使能进入，但不能得到重用。看看中国多少人才留在发达国家，看看中国多少人才留在私企，外企，大学，而不是留在政府，你就知道，中共政权快完了。有人可能要问，中国政府里，不是有很多人吗？不是有很多人才吗？中共政权里有很多人，但人才很少，而且，人才大量浪费，大量得不到重用。很多人才只好离开政府，下海经商。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 5.当一个政权的腐败已经使得其司法体系烂透了时，这个政权就快完了。因为司法体系，是社会公正的最后一道防线。如果司法体系都烂透了，人民在社会冲突中得不到公正的对待，一切靠权和钱来摆平，那么，人民对这个政权可以绝望了。政府只有维持社会公正，才有存在的必要。现在，中共政权不能维持社会公正，人民当然会抛弃它。看看中国有多少上访者，看看中国的司法体系每天制造多少冤案错案，你就相信中共快垮台了。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 6。当一个国家，人民的贫富差距太大，而且，这种贫富差距主要不是因为个人能力和努力的差距造成的，而是由于公权力被滥用所造成的，这个政权就快完了。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 7。当一个国家，不能搞好同邻国和强国的关系，并结交了一些狐朋狗友时，这个政权就快完了。中共与邻国如俄罗斯，南韩，日本，越南，印度，菲律宾，马来西亚，蒙古等关系都不好。世界强国如美国，日本，西方国家，都与中共政权不好。中国的确在敌对势力的包围之中。但是，中共却交了北韩，缅甸，伊朗等流氓国家，这些国家，除了要中共的资助外，实在对中国帮不了多少忙。你说，中共政权是不是快垮台了？<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 中共政权符合上面7个特征。你说，中共是不是快垮台了？<br />
