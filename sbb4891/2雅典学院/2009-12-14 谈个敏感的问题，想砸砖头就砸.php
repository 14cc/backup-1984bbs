<br />
好像有点乱，我的意思就是，这样混乱的社会，人是应该放下道德底线，出卖灵魂去赚取经济价值以实现自己的人生目标吗？楼下的请不要批判我，我不会比你们的道德觉悟差的，我只是困惑，为什么大家都提倡道德，但发现道德却是捆绑自己的工具呢？甚至有些人因为有道德，反而成了某些事情的牺牲品呢？道德本身是为了让人活得更好，但是在道德反而活不好，这种道德是不是就是伪道德？邓小平说，“端起碗来吃肉，放下碗来骂娘”，大家每天都在批判别人没有道德，自己却在做着不道德的很多事情，那这种道德不就只是皇帝的新衣而已吗？还需要这种道德吗？<br />
<br />
不仅是道德，其它价值观也是如此，人究竟应该持何种首先价值观在这个世上生活？有人想得清楚吗？<br />
