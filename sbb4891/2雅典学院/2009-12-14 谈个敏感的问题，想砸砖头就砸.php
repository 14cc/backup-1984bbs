<br />
不好意思，交待了这么多背景，我想问的是：人，活在中国，活在现在的中国，究竟应该秉持什么样的价值观与人生观？<br />
<br />
在学校的时候想得很单纯，想从事一份能给别人提供专业服务，为他人创造价值的工作，自己能从提供价值的过程中获得自己的经济价值与人生价值。<br />
<br />
最开始面试四大，也是觉得四大的工作最能体现专业性，也是创造价值的工作，而且是全球四大，诚信等人格底线不到受到挑战。<br />
