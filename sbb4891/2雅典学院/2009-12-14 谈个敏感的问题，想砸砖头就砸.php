<br />
学习成绩很好，我觉得自己智商很高，因为别人都上晚自习之类都考不好，我只上正常老师的课就考上了国内一所非常知名的大学。学的专业是随便挑的，一个很热门的学科。大学里面出了很多事，第一年有个老师要收贿赂，我们班没交，结果考试题目很难，全班仅几个人合格，我在不合格列，于是评优之类的我无缘了；然后又有一门课明明参加了考试，最后出成绩任课老师却说我没去参加考试，说没有我的试卷，最后没拿到学位。<br />
