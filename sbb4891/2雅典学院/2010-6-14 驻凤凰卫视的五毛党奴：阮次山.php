													<h2>驻凤凰卫视的五毛党奴：阮次山</h2>
						
						
																											<div id="postmessage_682413" class="t_msgfont">凤凰卫视阮次山其人<br />
<br />
阮次山，海南人，1946年生于广西，随讨生活的家人移居越南，所以有个越南人的名字，4岁随父从越南移居台湾，在台湾受教育长大，所以是台湾口音。1974年毕业于台湾台湾国立政治大学新闻系，学士学位；两年后赴美，获纽约圣约翰大学（St.John’s）东亚研究硕士，纽约大学（N.Y.U）政治研究所博士班研究。同时在美国的世界日报当编辑。据新浪网2008年3月23日的报导，阮次山当时工作非常不稳定，时而在台湾新闻界就职，时而在美国的中文报纸就职，时间都很短就又换工作。<br />
<br />
简历中的1981年至1986年，阮次山在美国《世界日报》任总编辑，但1986年突然去洛杉矶《中报》任副社长兼总编辑，直到中报垮台。<br />
<br />
随后10年阮次山的简历是空白。 <br />
<br />
接下去，1998年至2000年，阮次山在台湾发行的英文报纸《台湾英文新闻》任副社长兼总编辑。随后，又失业了。2001年，他不得不改行在香港一个朋友的商贸公司任个有也行没有也行的闲差：“顾问”。55岁的阮次山承认，整天替人家数钞票，他的人生走入了“低潮期”。<br />
<br />
阮次山不死心，一直在找机会，想再回到媒体圈子里，最好是挤进为官方服务的媒体里，因为那里狼奶充足，不用担心生计。<br />
<br />
各个香港媒体都跑遍了，还是没有下文。终于有一天，阮次山和凤凰卫视的前台老板并在总参任职的刘长乐挂上了钩，一谈到如何办好党媒的理念，两人一拍即合。刘长乐拍板定下，聘用他到香港凤凰卫视资讯台担任总编辑兼首席评论员。 <br />
<br />
2001年受聘凤凰卫视资讯台后，阮次山参与主持的节目有5个：《新闻今日谈》、《风云对话》、《VIP会客室》、《时事大参考》、《凤凰大视野》。同时还从事专栏写作。<br />
<br />
2008年，62岁的阮次山接受内地记者访问时说，他用业余时间，花很多精力，在中国大陆各地“无偿”演讲，倡导“爱国是个基本素质”。<br />
<br />
一次他在北大作“爱国”主题演讲的时候，一个北大学生一针见血的提问道：“您这样讲爱国，是不是在讨好当局？”阮次山承认，对这个忽悠不动的大学生，他“还是感到很失望”。<br />
<br />
在官方的鼓吹下，15所大学邀请阮次山担任了客座教授，他还在不少大学里演讲，倡导所谓的“大国民运动”，也就是他所说的“要求青年人应有一颗爱国心，要对国家有责任感”。记者问他，“为什么把着眼点放在大学校园里？”阮次山认为学生比较单纯，容易忽悠，他说，“我到大学里演讲，心想如果每次能影响几个人，十几个人，就很满意了。每次演讲下来，效果都不错，都能影响一批人。”<br />
<br />
阮次山敌我分的很清，说到美国，用“他们”；而说到中国大陆，用“我们”来表示。可阮次山本人却是美国公民，而且他还恬不知耻地对记者说“最喜欢的地方是美国”！<br />
<br />
在贵州采访，当阮次山深入到贫困地区，他感慨的不是政府、官员如此腐败，而是说：“国家这么大，发展不平衡，在中国当领导真不容易！”<br />
<br />
阮次山如此颠倒黑白，是欺骗不了外国人的，那么目地就很明确了，是专门来欺骗信息不灵通、不明真相的国人的。<br />
<br />
近年来，阮次山一直配合官方的对策，在中国大陆开展爱党的“大国民运动”，要求中国青年人应有一颗爱“国”的心，对党要有责任感。他说，“大国民的必要条件有：一是爱国，二是雍雅，三是关怀和感恩。”<br />
<br />
阮次山说，“爱国其实就是国家认同感，不爱国，如何成为国民的一份子”，“爱国是最基本不过的素质了，是作为国民起码的自尊”。<br />
<br />
这话怎么听怎么奇怪。阮次山是4岁随父亲从越南逃亡到台湾去，被国民政府收留。在台湾完成小学、中学、大学教育。28岁时毕业于台湾国立政治大学新闻系，拿到学士学位。两年后赴美留学，在美国纽约圣约翰大学得到东亚研究硕士，后又在纽约大学政治研究所博士班学习。<br />
<br />
阮次山受恩于台湾和美国。他为什么不爱国、不要国民起码的自尊？为什么对母国不关怀不感恩？<br />
<br />
阮次山是美国公民，入籍时曾宣誓效忠美国，但他却说了一段站在局外人才能说的话：“在美国，比我们更加强调爱国主义。小孩从小就接受爱国主义教育，从幼稚园就开始接受爱国主义教育，比如学生上课前对着国旗背诵效忠的誓词，其实他们比我们想象中的要热爱自己的祖国。”<br />
<br />
阮次山在此处依然用“他们”来代表美国人。<br />
<br />
难怪网友用37个字非常精练的把香港央视大忽悠阮次山给刻画出来了：看护照是美国人；看长相是日本人；看名字是越南人；听口音是台湾人。一开口，原来是党的人！<br />
