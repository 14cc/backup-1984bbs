																											<div id="postmessage_529439" class="t_msgfont">郑渊洁：我从1995年起就不信任疫苗<br />
2010-03-22 09:56:28&nbsp;&nbsp;21CN论坛 |&nbsp;&nbsp;<br />
<br />
<br />
&nbsp; &nbsp; 山西问题疫苗被揭露后，很多家长都担心自己的孩子的健康遭到威胁，有家长在自己的微博上写到，自己的孩子自出生以来，免费自费的预防针几乎一针不落，万一孩子第二天出了什么事怎么办。而早已为人父的童话大王郑渊洁从95年开始就不信任疫苗：<br />
<br />
&nbsp; &nbsp; 1995年12月4日，读小学的儿子郑亚旗放学回家后对我说：“郑渊洁，老师让明天交18元钱。”<br />
<br />
&nbsp; &nbsp; 我说：“知道了。一会儿给你放在书包里。”<br />
<br />
&nbsp; &nbsp; 吃晚饭时，我无意问了郑亚旗一句：“交钱买什么？”<br />
<br />
&nbsp; &nbsp; 郑亚旗说：“打针。”<br />
<br />
&nbsp; &nbsp; 我警觉地问：“打什么针？”<br />
<br />
&nbsp; &nbsp; 他说：“预防针。”<br />
<br />
&nbsp; &nbsp; 我感到蹊跷。在我们国家，给孩子打预防针都是免费的，学校为什么收费给学生打预防针呢？熟知通过老师向学生推销商品内幕的我打了一个激灵，莫非药品或卫生防疫部门也知耻而后勇地打起了学生的主意？我有一个原则，不管老师让我掏钱买什么没用的东西，我都酣畅淋漓地解囊。但是如果校方动通过往孩子身上注射药剂或口服药片的念头挣钱，我会殊死反抗。作为一个家长，当学校利用权力往你的亲骨肉身体里输入有可能毁了你的孩子的药物时，如果你不但不抗争保护孩子，反而提供经费，你还是父母吗？！<br />
<br />
&nbsp; &nbsp; 我向儿子要白皮书。老师每次收费都会给家长一张名正言顺的用白纸打印的信，我家戏称其为白皮书。儿子中止进餐从书包里将白皮书找给我看。白皮书上说是给学生注射“甲肝疫苗”。儿子从我的脸色上判断我可能会拒绝交费，他说：“我明天必须交钱。不带钱，老师会让我回家拿。”我说：“钱你照交，到打针那天，我给你请病假。这针咱们绝对不能打。谁为了经济目的往我儿子身上注射东西，我就跟谁拼命。”我儿子后来说，他从来没见过我的脸色那么难看过。其实这个道理很简单：生养一个孩子不容易。<br />
<br />
&nbsp; &nbsp; 次日，我匿名给北京市教育局打电话，向他们询问最近是否在全市小学给学生注射甲肝疫苗，回答是否定的。保险起见，我又给北京市卫生局打了电话，答复依然是否定的。为了杜绝冤假错案，再保险起见，我又给儿子就读的学校所在的区教育局打了电话，答复还是不知道此事。放下电话，我浑身颤抖，我不能不想起日本731部队。家长将生龙活虎的孩子送到学校，难道孩子一进校门就变成了任人宰割的牲口？<br />
<br />
&nbsp; &nbsp; 当天晚上我向儿子宣布，打针那天你不用去上学了。我将调查结果向他通报。往常每逢我不忍心看儿子受应试教育摧残而弄虚作假给他写假条让他获得放风的机会喘口气时，他都比较高兴。而今天他问我：“其他同学怎么办？”<br />
<br />
&nbsp; &nbsp; 我没听明白，问：“什么其他同学？”<br />
<br />
<br />
&nbsp; &nbsp; 郑亚旗说：“既然你知道了我们学校打预防针是为了赚学生钱，针剂可能是伪劣产品，你干吗不救所有的学生？他们也是父母费劲儿养大的呀？” <br />
我和儿子对视了足足两分钟一句话没说。我清楚我这次如果不制止他的学校给学生打甲肝疫苗，我这辈子甭想在他面前抬头做人了。<br />
<br />
<br />
&nbsp; &nbsp; 儿子又补充了一句：“如果是通过给全市所有局以上干部打针挣钱，你管不管无所谓。”<br />
<br />
&nbsp; &nbsp; 第二天，我以家长身份匿名给区教育局打了举报电话。放下电话，我担心区教育局忙于升学率疏忽我的举报，又给某电视台新闻部我的一位记者朋友打了电话，请他出面直接制止我儿子的学校擅自给学生打针。那朋友马上以电视台的名义给学校打电话，校方一听是电视台自然紧张，答复是打针系地区卫生防疫站通过校医联系实施的。记者朋友又给该卫生防疫站打电话核实，答复是此事纯属本站工作人员个人行为，没有接到文件。<br />
<br />
<br />
&nbsp; &nbsp; 次日，学校向家长退款。我再三叮嘱儿子，不能走漏是我破坏打针的风声，否则你在学校的处境会朝不保夕。儿子说当然得保密。<br />
<br />
<br />
&nbsp; &nbsp; 我不得不佩服记者的嗅觉。我以为事情已经完了，没想到几天后记者朋友来电话说，他继续调查了此事，甲肝疫苗一支才10元，学校敢收学生18元！他还说卫生防疫站可能是13元批给学校，学校每支干赚5元！他还说卫生部认可的生产甲肝疫苗的厂家有哪家哪家，其余的都是不合格药品。还说经他了解我儿子学校准备给学生注射的甲肝疫苗的生产厂家名不见经传。他说这样缺德的事如果不曝光后患无穷。我一听急了，说你敢！你爱曝哪所学校都没关系，就是不能曝我儿子的学校。你们新闻部的人都知道是我给你提供的新闻线索，你们去学校拍摄时，你怎么能保证你们的司机不和学校的司机聊天？一骂交通警就有共同语言无话不说了。如果让学校知道了是我搞的鬼给学校造成了这么大的经济损失，我儿子往后还怎么在学校混？我威胁他说你如果曝光我就和你断交。他权衡后选择了作罢。<br />
<br />
<br />
&nbsp; &nbsp; 1998年3月18日，山东单县卫生防疫站副站长宋新华自行携带碘钙营养片到单县城关第一完小推销。宋新华是每片9分1厘进的药，他卖给学校每片4角。学校卖给学生每片6角。3月24日至25日，学校将碘钙营养片发给18个班的1242名学生服用，至26日中午，先后有412名学生出现口干、腹痛、恶心、呕吐等不良反应，其中391人到医院接受治疗。<br />
<br />
<br />
<br />
&nbsp; &nbsp; 1998年1月8日，兰州市地方病防治领导小组、市卫生局和市教育局向全市中小学印发了《关于在中小学生中开展强化补碘及碘缺乏病监测的通知》，要求全市中小学生每人交28元至30元的补碘费，然后由学校向学生以每片0.77元的价格出售由市卫生局统一以每片0.10元向贵州兴义制药厂购买的海藻营养碘片。该药未经卫生部批准。截止4月11日，已有6300名学生服药后出现恶心、呕吐、腹痛等不良反应到医院治疗。 <br />
<br />
 1998年4月，邢台市卫生防疫站学校卫生科的王焕新以每片0.20元的价格卖给清河县教育局8万片陕西汉江制药有限公司生产的“速效肠虫净片”。清河县教育局加价后在4月15、16日两天以每片0.50元出售给全县学生每人两片，约有16000多名学生服用了该药。服药的当天，部分学生开始出现不良反应：高烧、腹泻、呕吐等。仅16日一天，就有4000名学生到医院治疗。清河第一小学学前班的学生也在劫难逃，17日，学前班一位女生在清河第二医院输液时对采访此事的记者说：“老师说了，不吃药也要交钱。我吃了就肚子疼。”<br />
<br />
<br />
<br />
<br />
&nbsp; &nbsp; 学校是教书育人的地方。如果学校将学生当作摇钱树，这还是学校吗？如今，我们的孩子事实上已经成为学校向家长勒索钱财的人质，有自己的亲骨肉在“绑匪”手中，哪个家长敢不老老实实交钱？如果我的儿子仍在那所小学上学，给我10个胆我也不敢写这篇随笔！一个老师曾经对我说过这样的话：“如果我对哪个学生或他的家长有意见，我治这个学生的最好办法不是体罚他或骂他，而是永远不搭理他！”我当时毛骨悚然。这在本质上属于撕票。<br />
<br />
<br />
&nbsp; &nbsp; 1998年4月，吉林省长春市朝阳区检察院破获一起特大贪污案。吉林省教委下属办公室的工作人员孙瑞星贪污公款200多万元。一个普通工作人员在不是创造巨额利润的企业的教委办公室居然能贪污200多万元！孙瑞星和宋新华肯定不认识，但他们两人却像捆绑销售的伪劣产品，在我眼前久久挥之不去。<br />
<br />
<br />
&nbsp; &nbsp; 家长之所以任凭学校搜刮钱财，说穿了是怕孩子日后上不了大学。为了孩子能上所谓的好学校或在学校受老师的器重以保证孩子将来跨进大学的门，家长面对学校忍气吞声忍辱负重什么都可以付出。但当学校要拿孩子的命挣钱时，家长也能付出吗？<br />
<br />
<br />
<br />
&nbsp; &nbsp; 其实，拥有大学文凭的真正含义是好找工作，而找工作的真正含义是给别人打工。因此，大学文凭实际上是一张加入打工族的特别通行证。没有大学文凭的真实含义是不好找工作，不好找工作的真实含义是逼迫自己创业当老板。因此，没有大学文凭实际上是进入老板序列的特别通行证。1997年美国《福布斯》杂志公布的美国最富有的400人名单中，有一支引人注目的没有大学文凭族，其中有中途退学的世界首富比尔・盖茨；有连初中都没读完就退学的克莱斯勒汽车公司大股东凯尔克・科考里安；有曾被布朗大学开除的现在拥有特纳广播公司的泰德・特纳；有上了一半大学就急不可待地退学的现在拥有世界上最大的废物处理公司的哈里・韦恩・休塞加……<br />
<br />
<br />
<br />
&nbsp; &nbsp; 作为家长，如果您希望自己的孩子将来当老板，他有没有大学文凭实在是无关紧要的事。任何一级工商管理部门在您的孩子创办企业申领营业执照时都不会因为他没有大学文凭而将他拒之门外。如果您希望自己的孩子日后当打工仔，大学文凭对他来说就是举足轻重的事，现在想当一个体面点儿的打工仔倘若没有大学文凭几乎是天方夜谭。<br />
<br />
<br />
&nbsp; &nbsp; 想明白这一点，希望能看到有决定让孩子将来当老板的家长于孩子在校时就敢对校方的所有不合理行为说不。真有这一天，我们的教育就出现了曙光。<br />
<br />
<br />
&nbsp; &nbsp; 在1995年12月，我可能救了一个学校的全体学生的命。<br />
<br />
<br />
&nbsp; &nbsp; 使我内疚的是，我没能同意电视台在1995年底利用媒体警告学校不要通过给学生打针吃药创收。真要是那样，也许今天就不会有如此多的孩子受服用碘片服用肠虫净片伪劣疫苗的荼毒了。上帝宽恕我。<br />
<br />
<br />
&nbsp; &nbsp; 最后，我给凡是不想白发人送黑发人的家长提个醒儿：当您的孩子告诉您学校要收费给学生打针吃药时，不管您昔日在孩子面前精心塑造了多么道貌岸然正人君子的高大形象，您在交费后也必须屈尊协助孩子作案在打针吃药那天逃学。否则孩子一旦遭遇不幸，巧妇难为无米之炊的您拿什么望子成龙？</div>

							
							
							
							
