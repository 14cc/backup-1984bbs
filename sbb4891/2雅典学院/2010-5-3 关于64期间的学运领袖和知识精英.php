													<h2>关于64期间的学运领袖和知识精英</h2>
						
						
																											<div id="postmessage_595901" class="t_msgfont">最近很多主帖讨论64，不好一一回帖，这里冒个泡。<br />
<br />
基于我对政治本身丑陋性的认识，我对任何政治领袖或者代言人都持有怀疑态度，不期待任何意义上的道德模范或者圣徒，对于处于弱势地位的异议方亦然。要我去认真仔细考证、考察、挖掘某某某本身是不是一个道德者，没有多少兴趣。<br />
<br />
对这些人不期待，不代表我会去攻击他们。群众政治事件是突发性的社会运动，它包括了当局、当局中的异见势力、学界左中右、民间代表，以及广大民众。其中民间代表危险与机遇最大，有点压上脑袋赌一把的意思。但是他们也是整个事件的焦点，换句话说，如果没有民间代表，其他各种势力仍然显性存在，但是最多仅仅是一种喧嚣和热闹，远远不是冲突。只有出现了一呼百应的民间代表，才是真正的冲突和对垒。<br />
<br />
民间代表的出现为民众承担了抵抗风险。从这个角度看，任何意义上对民间代表的道德批判都很缺乏基础。真正意义上的道德批判，只有德高者对德低者的诟病，也就是至少做到薄己厚人，厚己薄人本身就是反道德的。<br />
<br />
所以，对民间代表的批判可以是技术上的、策略上的，但不应该是道德上的。由于64的特殊性，民间代表都是乳臭未干的孩子，当事者基本上都能坦然承认技术上、策略上的失误。这就使得攻击者更容易选取道德方向，可惜这实在是一种错误。<br />
<br />
还有一个问题，“到底是英雄创造历史，还是人们创造历史”这个问题同样适用于那次政治事件。64不是某些民间代表鼓动起来的，当时的群情激愤之下，没有王、柴、吾等人，也会有其他民间代表脱颖而出，这点似乎有利于大家可以对他们进行某种程度的批判。但是，从他们承担的风险来看，我依然持反对意见。即使民众选择了错误的民间代表，也是环境和群众本身的不成熟。<br />
<br />
对历史的分析和挖掘是如此的困难，想当年的汪兆铭，为鼓革命士气，留下一段“不负少年头”的壮丽篇章。晚年的经历，也被不少人（或者说主流）钉上了历史的道德耻辱榜单。我们能够有多少自信来评价和攻击这些20出头的学生？<br />
<br />
相信自己的无知是知识的起点，厚人薄己是道德的底线，如果能够认清这两点，我想很多人不会这么气势汹汹、咄咄逼人，反过来可以思考一下自己能够做些什么。</div>

							
							
							
							
