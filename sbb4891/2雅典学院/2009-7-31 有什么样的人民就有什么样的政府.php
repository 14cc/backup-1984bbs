																											<div id="postmessage_191350" class="t_msgfont">ref：<a href="http://www.ipencil.org/?p=3841" target="_blank">http://www.ipencil.org/?p=3841</a><br />
高王凌是人大清史所的一位学者。在研究中，他发现了人民公社时期中国农民的一种重要行为，他称之为“反行为”。这是一种不公开的小规模反体制行为，比如隐瞒产量，少缴公粮，在劳动时小偷小摸，在分粮时设法多分，向公社生产队借粮，然后不了了之，等等。<br />
<br />
这些行为，都不是对人民公社制度的公然反抗，但无疑是对这个制度的侵蚀和破坏，并违反了传统的道德。这些反行为，缓解了不合理的公社制度，让农民不至于被饿死。在那个贫困的年代，反行为几乎能为农民带来一半甚至更多的粮食收入。如果没有这些行为，很难解释当时那些农民是怎么活下来的。要是严格按照人民公社的制度设计和上级的要求缴纳公粮、诚实劳动，肯定有更多的人饿死。<br />
<br />
当地的公社或者生产队干部对农民的“反行为”是心知肚明的，那么，他们为什么不进行干预和管理呢？干部的干预和管理是有的，有时还很激烈，甚至成为政治斗争，其间还有致死人命事，但总体来说，这些事情是不容易管理的。一方面，都是乡里乡亲，难道还真能让人饿死不成？另一方面，许多“反行为”，并不是严重、明显的盗窃或者抢夺，介乎占便宜和手脚不干净之间，管理不可能滴水不漏。<br />
<br />
这种情况其实不难理解。比如，职员不应该侵占公司的财产，但在写字楼上班的人，又有谁从来没用过公司电话谈过私事呢？有谁没用过公司的几张纸打印自己的东西呢？有谁没在上班时间上网聊天看股票呢？<br />
<br />
这些行为，严格来说都是在偷公司的钱，但在现实中，即使是老板，也不好就把这些行为称为“偷”，当事者更不会认为自己是在“偷”。大家只会认为这只是不值一提的小事。<br />
<br />
农民的反行为也大致如此。在收割时顺手拿一些粮食回家，到公社的水塘里捞一些鱼虾，分粮时在秤上做一些手脚，劳动时设法出工不出力。这些事情，在那个饥饿的年头，实在不能算是多么恶劣的行为。大家都认为，那不过是一些不值一提的小事。<br />
<br />
高王凌在实地调查时就遇到了非常典型的情况。某村，人们都承认有这种“反行为”。但附近的另一个村，人们矢口否认。调查者很意外，不相信这个村的人就那么纯洁。细问之下才发现，如果你问人们是否“偷”过，大家都会否认，如果你列举出那些具体的“反行为”，那人们就会恍然大悟，哦，你说的是“抓握” 啊，那是有的。<br />
<br />
也就是说，人们并不认为那些行为是什么“偷”，而只是“抓握”。词语的变换非常重要。换了一个词，也就解除了人们的道德压力。<br />
<br />
农民的“反行为”从理论上来说，正是波兰尼上述理论的现实表现。<br />
<br />
对人民公社的干部来说，公社的利润和他个人的收益没有直接关系。他只对上级领导负责。他的上级对更高的上级负责。同时，公社的许多生产事务都是中央统一规定的。可见，人民公社的最终管理者是高高在上的中央。这是一个典型的中央计划管理体制。那么，中央能不能实行有效的管理呢？当然不能。中央只能委派当地干部进行管理。<br />
<br />
和现代企业中的职业经理人不同的是，当地干部的责权并没有严格的界定，因此，他没有激励去追求公社利润的最大化。只是在少数中央明确指出的事务上，地方干部才会进行有效的管理，这就是政治运动期间的某些具体任务。除此以外，地方干部对中央也仅仅是敷衍而已。他们是被动的，也是中央管理的对象。<br />
<br />
于是，问题仍然是中央是否能有效地管理广大地方的事务。这当然超出了任何人的能力。于是，大量的层次和范围实际上都处于无管理的状态。就是在这些层次和范围，农民的“反行为”广泛存在。利用这些“反行为”，农民事实上局部化解了人民公社制度，为自己谋得了生存的机会。<br />
<br />
可以把“反行为”理解为农民的一种消极反抗，但更有理论意义的是，“反行为”的存在和难以制止，以及由此导致的生产停滞、产出减少和道德衰落，实际上都表明了社会主义计划经济制度的失败和不可能。这种制度，建立在无知和狂妄的基础上，为管理者划定了不可能完成的任务，而且，由于政治垄断，其他的社会组织方式被强行压制。结果就只能是，管理者假装在管理，而民众假装被管理。如苏联人所说：“我们假装工作，他们假装给我们发工资”。真正起作用的还是某种自发调节的秩序。<br />
<br />
那么，农民的这种“反行为”和上述公司中职员小小地侵占公司利益有何区别呢？区别在于，公司虽然也会有一些难以彻底清除的浪费和侵占现象，但这些现象都没有达到妨碍公司正常业务的程度。公司把这些损失计入正常的损耗成本中即可。<br />
<br />
而人民公社中广泛存在的“反行为”，是与农民严重缺乏生产积极性密切相关的。农民甚至在这些“反行为”上倾注了更大的精力，这些“反行为”为他们带来了更多的实际收入。相比之下，正式生产反倒被故意忽视。也正因此，虽然“反行为”广泛存在，但农民也只能得到勉强温饱的水平而已，进一步的富裕和发展是谈不上的。<br />
<br />
在城市中的国有企业中，工人也必定存在大量类似的“反行为”现象。国有企业中的工人有各种办法出工不出力，以表面上的完成任务掩盖实际上的偷懒休息，并想办法偷拿企业财产。“外国有个加拿大，中国有个大家拿”。在这方面，国有企业的管理者同样无能为力。<br />
<br />
如果是私人企业，一方面管理者有更强的激励加强管理，另一方面，管理能力将制约企业规模。如果不能同时发展出有效的管理办法，企业就不会扩大。如果企业规模超出了管理能力，那就只好把企业拆分。对于自负盈亏的私营企业来说，规模一定是盈利能力和管理能力之间的平衡。<br />
<br />
计划经济者无视这种平衡，甚至要把整个国家变成单一的企业，这只能严重超出管理能力的限度，导致经济单位的管理水平低下、秩序混乱、产出萎缩，“反行为”成为常态，不道德的行为成为人之常情。这就是我们在几十年的社会主义历史中看到的现实。<br />
<br />
这也解释了为什么社会主义还能存在几十年。实际上，真正的社会主义制度至多只能存在一两年，甚至更短。然后，社会主义者就不得不在事实上放弃他们的理论，他们的目的从改造社会转向垄断政权。由于他们继续垄断政权，就让人误以为社会主义制度还存在，其实，存在的仅仅是一个名称和外壳而已。<br />
<br />
至于内部，社会早已不可避免地恢复到某种多中心、自发调节的秩序中。观察农民的“反行为”，不难发现其中的某些被广泛接受的规则。只不过，由于政治因素的干扰和破坏，这种自发调节秩序非常不稳定，时时要被破坏，人们难以建立起稳定的预期，因此也就不愿意做出更多的人力物力投资，秩序也就不可能向更复杂、更高效的程度发展。反映到现实中，就是可悲的停滞和衰败。<br />
<br />
社会主义国家那个被严重破坏的自发秩序只能提供很基本的价格信号，维持很低水平的社会的基本生存。如果想要进一步发展，社会主义国家就必须亦步亦趋地从市场经济国家那里获得组织生产的办法和价格信号。在社会主义国家，所有高级复杂的生产都是学来的，都是引进的，而且一旦引进就被固定下来，不可能继续演进和发展。<br />
<br />
苏联的社会主义建立于1920年代。他们效仿当时美国的生产体系，也就是大工厂、流水线、机械化的福特生产方式，然后就再无变化。一直到解体，苏联都在沿用这种生产方式。如果布尔什维克更早一些出现，他们也许会把英国工业革命时期蒸汽机的生产方式一直保留下来。<br />
<br />
社会主义者往往把他们的失败原因归结于其他人不响应他们的号召，不和他们一起搞社会主义。但愿他们能理解，如果其他人真的和他们一起搞社会主义，如果这个地球上不再有市场经济国家，那么，无需核武器，人类早就返回茹毛饮血的野蛮时代了。</div>

							
							
							
							
