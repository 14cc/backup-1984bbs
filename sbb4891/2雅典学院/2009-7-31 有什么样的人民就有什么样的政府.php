													<h2>有什么样的人民就有什么样的政府</h2>
						
						
																											<div id="postmessage_186188" class="t_msgfont">政府生于社会，不是独立于人类社会的存在物，这就决定了有一个什么样的社会就有一个什么样的政府。而社会是由民众构成的，进而决定了，有什么样的人民就有什么样的政府。一个具有自由精神的民族，一定会建立一个自由公正的政府；而一个充满奴性的民族，则会屈服于强权的统治。这就是说，政府的性质取决于它所内生的社会的性格。<br />
&nbsp; &nbsp;&nbsp; &nbsp;<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; 事物往往会在发展中异化，人的发展也会异化。人造了佛，会拜在它的脚下；人造了汽车，会觉得开汽车的比走路的威风；同样，社会造就了政府，有时会称为被统治的奴隶。<br />
&nbsp; &nbsp;&nbsp; &nbsp;<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;在对政府的种种不是的批评当中，有不少已经忘记了政府内生于社会，默认政府是外生于社会的，和社会是对立的。这种思想，其实不是自由的思想。这种思想是一种将政府当作外来之外的思想。在批评当中，将自己独立于社会之外，似乎自己已经不是生成政府的社会的一员了。<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; 八十年代初，北京一群大学生的一个社会实践活动，最后得出的报告的结论是，这个社会要想搞好，首先要&quot;人人从自己做起&quot;。这个结论无疑是正确的，但是却是最最难以做到的。古训道：己所不受，莫施于人。自己不愿意做到的，就不要强求他人做到。有人反对贪污腐败，自己上台后贪得更多；有人反对为富不仁，自己发达后对为富不仁特能理解；有人反对徇私枉法，自己家人进了监狱就拼命到处活动。是为狐狸品格：吃不到葡萄就说葡萄酸。&nbsp;&nbsp;<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;如果有人认为惟有自己清白清明超凡脱俗，可怜了，你恐怕是生不逢时了。如果想改变这个你生不逢时的社会，你就要做好为你自己的理想牺牲的准备，否则，你就安分守己认命吧。<br />
