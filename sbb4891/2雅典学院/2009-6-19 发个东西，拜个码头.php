按照一般的标准，此言证据确凿的从该副局长嘴里冒出来，接下来的戏码大致应该是停职检讨了。虽然对于很多官员犯事儿之后事情我们并无信心能够看到其最终下场凄惨，但风口浪尖的时候，即使是出于避风头的缘故，这种处理也是常用的手法。只是这次略有不同，在否认情况之时，当地组织部放言曰：逯军此言属于个人言行，只能代表个人，组织部也管不了，国家也是规定言论自由。<br />
请让我们脱帽肃立，为国家所规定的言论自由默哀。言论自由竟然为这么一句话殉葬，真是令人无语凝噎。毫无疑问，国家确实规定了公民有言论自由，只是这个自由是不是适用在这里，恐怕相当难说。<br />
从说这话的场合来说，那是该副局长的办公室，他所面对的是记者，解释的是一件公务行为。在公务行为里没有个人言行，他能够坐在那里，就代表了一级政府机构的权力。任何从他嘴里说出来的话，只要是关于这个事件的，就代表政府的意见。换句话说，要是他在酒场舞厅之时说出这种话，即使不算妥当，也算是一种个人的行为。而当地组织部门如此说，倒是让我们知道了一件事：该副局长敢于如此大放厥词，大致是因为组织部门的这种态度吧。<br />
在当地组织部门看来，即使官员在工作场合把话说到了这个份上，那也是算个人的言行，他们是没有权力干涉的。不知道在当地组织部门的心中，如果官员在工作场合收取贿赂，是否也是个人行为？是否汝等也是没有权力干涉的？这也无怪当地能够把经济适用房的项目建成了别墅，原来这可能在当地的某些机构眼中也是个人的行为，也是具有国家所规定的自由。<br />
在此事刚刚出来的时候我就说过，这是该副局长认为自己的权力是从上面批发而来，自己的行为即有上峰权力的认可，又有自主性的代表所导致的昏话。现在看来这是不假的。从该地组织部的言行来看，确实这个权力是被他们批发出去了。连该副局长在职务场所、以职务身份接受采访都成了“个人言行”，这要不是权力批发后由下级经销商随便定价的话，如何又能出现呢？<br />
