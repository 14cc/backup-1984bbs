													<h2>江小鱼：“低人权优势论”错在哪里?</h2>
						
						
																											<div id="postmessage_700631" class="t_msgfont">南非世界杯前夕，秦晖教授抛出了一篇长文《南非经济奇迹的背后》，当时我在网上见到，但并没兴趣阅读。后来，关于工会工潮的争论激烈，有记者也提及此文，我便从头到尾读了次，并顺便把他之前重复说的“低人权优势”的几个文也看了。<br />
<br />
把南非换成中国，黑人换成农民工，种族隔离换成户籍制度和城乡差别，再把里头的数据换成中国的统计数据，其他基本不用怎改，这依然是一个“低人权优势”的重复唠叨，以至我阅读的时候常会产生思维错觉，觉得每句话说的都是中国。<br />
<br />
争论的必要前提，是要理清概念分歧。要辨析所谓“低人权优势”，我们就要弄明白什么是人权。严复把密尔的《论自由》翻译为《群己权界论》，这个名很好，一个人的世界本无所谓的自由和产权，也无交易费用，自由就是要把人和人之间的权利分清楚。而自由、人权、产权，其实是同一意思，离开了产权去谈人权，越是纵横捭阖，越是离题万里。<br />
<br />
然而，如果说人权本质是产权，秦晖应绝不会同意。因为这词不是被抬高就是被歪曲了。如前所说，人权即自由，也即是人与人之间的权利界定，然而，众口所说的人权，已经变味成了个人与政府的关系，使得它带有某种自然正义和悲壮的色彩。至于那些发明了“免受贫困的权利”（或者是“免受匮乏的自由”）、“居住权”、“健康权”等千奇百怪词语的人，更是抛离本意而单从个体来阐述权利。你有免受匮乏的权利，就意味着存在一个他，有付钱给你脱贫的义务和责任；你有买得起房子的权利，就代表有人花钱为你供楼。<br />
<br />
当然，因为有个政府的中间角色，使得问题不那么简单。在2008年一个采访里秦晖如是说：<br />
与此同时，要求政府做该做的事，尽该尽的责。在中国即使要求政府负责从摇篮到坟墓的福利也是应该的，因为你的权力是不受限制的。既然政府不承认自己的权力有边界，那么我们也可以认 为其责任也是没有边界的。反过来说，你要求我的问责有边界，我也要求你的权力有边界。<br />
<br />
可以说，这种观点很有代表性，要知道，并不存在完全不受限制的权力，也不存在天上掉下来的福利。认为政府权力过大，因此就要它去剥夺一部分人的权利，去补贴另一部分人？这无论如何也说不通吧。自由派和左派的分野也正在于这里，政府权力过大，那就应该限政，不要让它事无巨细都插手，要通过看不见的市场之手，把自由从看得见的手那里抢过来。而左派的荒谬之处，是认为政府权力过大， 反而去赋予它更多的权力：政府要解决人们的贫困问题，保证他们的生活质量，有尊严地活着；政府要使人们能够购买得起房子，买不起的也要提供房子给他们住。<br />
而福利主义和民粹思想盛行，其根源是平等主义作祟。正因为许多人把这些都作为人权论的一部分，他们才会错误地认为，经济越发展，人权越糟糕。是因为经济发展，必然会拉开收入的差距。这也是极左们认为昔非今比的根本原因，他们甚至会认为，改革开放前远比现在高人权。我不敢贸然认为秦晖也是这样想，但至少他的思想是有这种痕迹，纵然他被许多人认为是中国自由主义的代表人物，然而我却认为他实际上是左派代表。<br />
<br />
在解决基本的词义问题后，我们再来看所谓“低人权优势”。<br />
这个词语，应解读为“因为低人权，所以有优势”。这种逻辑正是典型的左派思维。推到极致，如果这种优势存在，那么北韩经济应该很有优势，为何资本没有大举进入？左派会反诘：那是因为资本不允许进，否则就有优势。<br />
<br />
问题就在这里！资本不允许进入，本身就是最大的低人权。中国的问题，南非的问题，一样如此，在改革开放之前是更低的人权，垄断程度更高的国企，更为腐败的政府。<br />
现存的一些问题，权力寻租、暴力拆迁、司法体制，都会影响到个人的权利。然而，即便是他们所说的这些社会顽疾，相比较以前仍有进步。开放初期，每个人都小心翼翼如履薄冰，买东家卖于西家就是投机倒把。再说最为人诟病的拆迁问题，城市改造这个问题是难免的，现在遭遇非法拆迁的时而有之，毕竟大多数人能得到补偿，越开放的城市，会相对公开与公平。然而，以前一纸调令就可以轻而易举把你从一个单位掉走，从一个城市调到另外一个城市。谈何云“权利”二字？<br />
<br />
资本进入，经济得以发展，个人的收入增加，选择就多了。秦晖谈南非问题时，也承认了“持续经济增长使得哪怕是在这种体制下受欺负的黑人，生活也在提高：纵向看，他们比以前要好；横向看，他们比周边那些黑人掌权的邻国，大众生活水平也高出一截。”这点总比毛派好。他的问题在于认为人权是通过民主争取来的，而资本发展是利用了低人权，并且可能阻碍人权的进步。<br />
事实上，就说南非，正是资本的进入大大改善了黑人的生活境况。可以说1994年曼德拉能够成为第一位黑人总统，他的非暴力政治能成功，也正是得益于资本流动所创造的开放环境。南非对外开放融入全球化体系后，经济取得很大进步，这个数据秦晖也有列举，<br />
<br />
从GDP来 看，南非1932年仅为4.66亿兰特，1972年达到150.52亿兰特，到 1980年已达592.00亿兰特。<br />
然而，这个增长却成为他“低人权优势”的论据：<br />
于是南非黑人的低人权状态在全球化市场体系中便戏剧性地成为加快资本原始积累的有利条件。在铁腕控制形成的“稳定”局面下，没有工会、缺乏博弈能力、吃苦耐劳而又百依百顺、既低工资也无福利“成本”的廉价劳工。<br />
<br />
对于工会问题，经济学家应有共识，工人与资本家是一种契约关系，动辄说“剥削”，是知识的不足。资本也不可能靠低人权来加快原始积累，只有当一个国家稳定下来，人民有所预期，契约能得到保护，资本才会大举进入。动荡乱世，风险畸高，不排除有偏好高风险的资本进入，然而那永远不会是个普遍现象。<br />
<br />
而政治稳定，合约能得到保护，这些恰好是人权改善的标记，并非是秦晖眼里的低人权。比如，对于目前的北朝鲜来说，任何欢迎资本进入的举动都可以视作一种进步。人权的进步植根于个体经济的独立，如果某一天，朝鲜能如同中国和南非一样，让外资进入，办起遍布全国的“血汗工厂”，这个国家就有希望了。当然，那个时候，秦晖也会继续抛出文章，抨击朝鲜的进步是通过压制人权来取得的。</div>

							
							
							
							
