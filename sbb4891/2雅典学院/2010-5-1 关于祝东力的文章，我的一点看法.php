													<h2>关于祝东力的文章，我的一点看法</h2>
						
						
																											<div id="postmessage_593070" class="t_msgfont"><a href="https://1984bbs.com/viewthread.php?tid=44605&amp;extra=page%3D1%26amp%3Bfilter%3D0%26amp%3Borderby%3Dlastpost%26amp%3Bascdesc%3DDESC" target="_blank">https://1984bbs.com/viewthread.p ... mp%3Bascdesc%3DDESC</a><br />
<br />
单拿出来，听听大家的看法<br />
<br />
原文虽然没有针对自由派人士，但的确严肃的提出了若干挑战。<br />
<br />
真是有心人，就应该早早脱离盲目给人贴标签的童稚状态，严肃的考虑对方提出的这些挑战，至少应该予以认真思考和正面回应。<br />
<br />
并不是你给人家一顶党奴五毛之类的帽子，人家提出的问题和挑战就不存在了。<br />
<br />
比如，中国老百姓最关心的是什么？不是民主和自由的价值是否能得到实现，而是<br />
1）民生问题，福利保障；<br />
2）腐败问题，贫富差距及其带来的不公平的感觉；<br />
<br />
自由派人士的主张，要么对上述问题漠不关心，只是一味空喊正义自由价值一类的空中楼阁，忙着纪念林昭，老百姓关心么？就算林昭投胎转世当了中华民主国总统，且不说会不会陈水扁话，就算她个人操守不染，有助于解决上面的两个问题么？<br />
<br />
要么就是卖自由民主大力丸，盲目腔调只要实现了民主制度，腐败问题矿难问题贫富分化问题就会迎刃而解 -- 这种朴素的观点其实也正是很多出身社会底层的知识分子对自由民主真正感兴趣的原因。但这种看似直白的逻辑，并不一定经得起推敲，至少需要经过实证检验。比如，卖大力丸的经常拿发达国家说事，但却常常可以回避有更多的民主国家腐败不亚于甚至超过中国这个事实，如原文红子部分列举2008年透明国际的数字。要研讨民主制度同腐败的关系，你不能看<font color="red">不腐败的国家中有多少是民主国家</font>，你真正应该关注的应该是<font color="red">民主国家中有多少是腐败的，多少是不腐败的</font> -- 这样才有量化评估的基础，通过评价民主化的预期利益（假设存在可计算的民主国家腐败率，即可对民主的预期收益进行评估）然后再同中国民主化所带来的风险进行对比，这样才能进行有效的决策。<br />
<br />
可我见到的自由人士，都是空喊口号的多，真正严肃认真的把民主化当做一项事业，作为一个project来进行严肃考量的，少之又少。<br />
<br />
最后一段，祝东力尝试给出了一个自上而下的解决方案 -- 颇有给土共当局献策的色彩。对于仇恨土共人士，你大可诅咒他的八辈子祖宗，因为他给土共献计献策，不管目的是为了邀功买好还是为国为民，客观上总是有利于维护稳定土共统治 -- 你要骂他，我没意见 -- 这就好像文革时那句有名的口号“清官比贪官还坏”一样。但不管怎么说，他这个两步走（<font color="red">维护民生发展福利缓解矛盾以安抚民众 + 缓慢但是主动有意识的提供民主消费品以安抚知识精英</font>）的策略内容，是值得认真思考的：<br />
1）他这个设想价值如何？如果土共真能做到了，是否真能像他说的这样咬牙30年苦尽甘来？<br />
2）土共主观意愿如何，土共有这个动机么？<br />
3）可行性又如何？假使愿意，土共做的到么？国际国内的环境允许么？<br />
<br />
比如，92年以后，土共仅仅以分一杯羹的方式，仅仅靠着物质奖赏，即可招安绝大多数的知识精英；那么假如土共在看得见的将来，不仅仅提供物质奖赏，还提供象“听证会制度”，“官员问责制”，甚至一定程度上的容忍和平的抗议与维权（如同厦门PX项目）这一类<font color="red">“民主消费品”</font>的话，这是否意味着土共将有能力网罗99%的知识精英 -- 到时候，就算关二想造反，就算有几千万对现实严重不满的底层百姓，也会因为没有知识精英的组织参与而迅速瓦解（就像韩寒说的，老百姓上街，你发给他们每人10块钱，他们就回家了），土共的统治岂非千秋万世？<br />
<br />
抛砖引玉，待有心人讨论。<br />
这两点都是值得严肃思考和讨论的。<br />
