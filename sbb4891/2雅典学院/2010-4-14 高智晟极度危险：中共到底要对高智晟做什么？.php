													<h2>2007年4月1日任不寐一封信谈高智晟事件（摘要）</h2>
						
						
																											<div id="postmessage_567484" class="t_msgfont">……这几天，我的信箱里还塞满了另外一些相关信息：批判高智晟的运动仿佛又掀起了新的高潮，而我印象中，这件事情不久以前好象是已经过去了。你知道这卷土重来是怎么回事吗？一些来信让我再为高智晟说几句话，而我实在不想再说什么了。因为“文化商业主义”根本不想面对真相，他们只想通过毁灭他自我荣耀。你不可能叫醒一个装睡的人。<br />
<br />
<br />
<br />
想起高智晟我心里有一种特别想流泪的感觉。不仅仅是为他这个人，也是为那个叫“中国”的类概念。如果这“中国”也有心灵的话，那心灵就仅仅是伪善、野蛮和愚蠢。那种毁灭高智晟的舆论导向甚至开始影响曾经大力支持他的人，这就是中国――机会主义无处不在。你知道，我不站在支持高的和反对高两个宗派任何一边，我只是爱高智晟“这个个人”（齐克果）。那两边的很多“伟光正”人士同样让我叹息不已，敬而远之。<br />
<br />
<br />
<br />
一个为一些正义感突然挺身而出奋不顾身的人，就这样在文化商业主义思潮的围剿中孤零零地被贬到道德劳改营的或文化上的偏远农村去了。这样干的并不仅仅是国家，甚至主要不是国家。在专制社会，国家的敌人正成为民族的英雄，但在高智晟身上，这一逻辑不发生作用：国家的敌人也成为人民的敌人，至少成为“网络自由独立作家们”的敌人。一个本来该被整个民族感谢和同情的人，反而成了这个民族的精英人物嘲笑和边缘化的对象。几乎再没有一个有力量的声音还记得他的贡献，整体性的落井下石和见风使舵令人无地自容。高智晟几乎快成了反面人物，而之所以如此，仅仅因为他为正义呼喊过，只是没有站在文化商业主义阵营那里呼喊。与此同时，在高智晟灰溜溜的背影里面，那些为否定他做出过贡献的，甚至焦大牛二之类精明至极的网络流氓，当然还有利用这些可怜工具的意见领袖之类的角色，开始按劳分配荣耀。<br />
<br />
<br />
<br />
我从来不认识高智晟，也没有和他有任何只言片语的通信。但我认识人，我知道人所共用的那些缺点和优点，在他身上都不同程度地存在着。在这些因素上建立对任何人的论断，没有一个人能站立得住。我在这里退一万步说，即使高智晟在里面彻底“软弱”了，这仅仅使我更同情甚至更爱这个人，但一点也不形成对这个监狱里的人的任何道德否定。然而，中国的“文化商业”集团，这个为名利双收而形成的“写作班子”们，正是以此为基础安身立命的。否定他的人坚持不懈地批判他的软弱，支持他的人断断续续地否定他的软弱――然而高智晟是否在监狱里软弱了（以及其他一些“个人缺点”），根本不是评价这个人的主要根据，也根本不是任何心里正常和健康的人应该引为褒贬的对象，更不是哪怕拥有一点点人类同情心的“网络作家”（她们因为把网络当场公共厕所而成为“作家”）和网络媒体用来幽默、作文甚至损人利己的素材。高智晟软弱（或者有哪些缺点）了，这本身就是一个假问题――即使高智晟就是软弱了，那又怎么样呢？！<br />
<br />
<br />
<br />
<br />
这种普遍的有病状态，不仅仅来自恐怖对人性的摧残，更来自利益对人性的扭曲。反思恐怖对人性的摧残，导致自我忏悔。而利益对人性的扭曲，则导致对他人的个人否定，甚至逼人忏悔。这是一种特殊形式的自我辩护，一种变态的自卑。这个国家除了在别人的伤口上撒盐这一文化习惯以外，已经彻底丧失了人类的同情心和宽容。有的时候，恰恰是因为国家在他（她）的伤口上撒了盐，他（她）就必须让更多的人分享他（她）的“荣耀”。所有文化商业主义者根本不是文化疯子，而仅仅是文化灾民。他们再怎么装疯卖傻、装嫩耍贫，或者一脸正气、满腔忏悔，见风使舵的宽容表演，都是一种报复。同时，在灾民社会，一点蝇头小利都会把“网络看家”变成看人喝彩和看人喝倒彩的托儿，这就近年以来“网络自由主义”的全部贡献了。<br />
<br />
<br />
<br />
<br />
……这种风潮极其工于心计，就是千方百计影响更多中立的人的态度，因为可有可无的传闻同样会动摇别人对他的尊重。一位美国总统候选人在竞选的时候指责对方与野猪发生性关系，后来记者问他：你凭什么证据来指控。他回答说：我没有任何证据，我只是想让舆论看到他们为自己辩解时候的样子。我常常感慨，如果不是十字架的缘故，为这样的人类预备地狱，真有什么不公平吗？<br />
<br />
<br />
<br />
……不过在某种意义上，要特别感谢批判高智晟这场蔓延了两年的网络风波，它让我们看见所谓的中国自由主义运动和公共知识分子或持不同政见者那点出息，并回想起他们更多的“政绩”。特别值得一提的是，中国的持不同政见者制造了另外三个持不同政见者：一个是高智晟被批倒批臭，另一个郭飞雄被关进监狱，还有赵昕被软禁在家中。与此同时，“著名作家们各种自由主义舞会”和中南海各种政治舞会垄断了全部的信息资源。</div>

							
							
							
							
