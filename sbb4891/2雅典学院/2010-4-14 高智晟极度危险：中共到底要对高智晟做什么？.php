(博讯 )<br />
<br />
&nbsp; &nbsp; 高智晟声称为了家庭放弃维权，令很多人对这个曾经的硬汉感到失望。但是大家想一想，高智晟这些年受到了多少非人的折磨呢？高智晟即使不声称放弃维权，在警方的严密监控下他又能做什么呢？<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp;&nbsp;而值得我们反思的是：为何高智晟以悲剧收场？ <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;１。海外民运坐山观虎斗<br />
&nbsp; &nbsp; 在高智晟发起的一系列维权活动中，海外民运(除了香港)并没有在海外造势配合，基本上都是以旁观者的态度关注。虽然也有口头上的支持，却没有任何行动的表示。给人的感觉是：看热闹。<br />
&nbsp; &nbsp; 如果我是高智晟，看到自己（和家庭）在孤军奋战，其它人在看热闹，我也会心灰意冷。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; ２。错过海内外联合的良机。<br />
&nbsp; &nbsp; 在高智晟发起的一系列维权活动中，有一个良机可以迅速推动中国的民主监督进程。那就是海内外联合起来，组织一个国内30省市，海外30城市的轰轰烈烈的&quot;维护中国人基本权利&quot;&quot;要求官员公布财产&quot;的大型活动。<br />
&nbsp; &nbsp; 这样的活动，国内的活动不需要多（安全原因），主战场在海外，国内只要呼应就可以。这样的大型活动如果组织起来，对中国政府的压力可想而知了。如果这样的活动持续下去，中国政府该怎么收场？<br />
&nbsp; &nbsp; 可惜的是，良机已逝，即使现在有人愿意在海外组织，国内还有没有一个新的高智晟呢？<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; ３。法轮功的支持力度不够。<br />
&nbsp; &nbsp; 法轮功把更多的精力放在了讲真相和退党。殊不知，中国老百姓最关心的还是维权，民权，和推动民主监督法制。如果不从细节入手，不从替老百姓争取民权入手，很难获得国内民众的支持。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; ４。中国政府的高明手段。<br />
&nbsp; &nbsp; 国内的民运人士一出头就被镇压，而海外民运特务横行，一盘散沙，夸夸其谈。可见，中国政府对付民运人士的策略是很成功的，不得不令人佩服啊。祝贺中国政府，这次你们又成功了。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; ５。高智晟错误估计了形势。<br />
&nbsp; &nbsp; 高智晟起初以为中国政府在国内大失民心，再加上海外华人的觉醒和互动，中国的民主进程可能在短期内就会有大的转变。<br />
&nbsp; &nbsp; 高智晟显然错误估计了形势。海外民运的不作为，国内的严控的形势，目前没有对中国政府造成任何的压力。<br />
&nbsp; &nbsp; 现在高智晟可能也醒悟过来了，中国人做实事的没几个，夸夸其谈的一大把。按照这个形势，中国的民主进程不倒退就不错了，跟别提进步了。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 无论如何，从高智晟当年的意气风发，到现在的意志消沉，我们也许该读懂点什么。一个英雄最后不得不妥协，不得不以悲剧收场，谁的过错？</div>

							
							
							
							
