													<h2>余杰：西藏就是潘多拉，王力雄就是杰克，唯色就是纳特莉</h2>
						
						
																											<div id="postmessage_460008" class="t_msgfont">我在推特上说，我要用这个题目写一篇关于《阿凡达》的评论。立即有推友回复说，这是史上标题最长的影评。我说，不是我的标题太冗长，乃是西藏的苦难太漫长。《阿凡达》是一部民族史诗，是所有弱小民族的史诗，是被西班牙殖民者屠戮的玛雅人的史诗，是被纳粹屠戮的犹太人的史诗，是被中共屠戮的西藏人的史诗。真理部竭尽全力编造的一切的谎言，在这部比现实生活还真实的电影面前不攻自破了。如同进入梦境的杰克，逐渐觉得梦境笔现实还要真实一样；今天仍然生活在“一九八四”的中国人，只能在电影中才能找到真实的人生。一九五零年中共军队侵入西藏之后六十年的历史，居然被浓缩在这部不到三个小时的电影之中，三个小时等于六十年，由此可见卡梅隆不愧为有史以来最伟大的电影导演。当作为前海军陆战队士兵的杰克，毅然放弃人类卧底的身份，而选择成为纳索族的一员，不惜顶着叛徒的恶名、冒着生命的危险，揭竿而起、反戈一击的时候，在黑暗的电影院里，我不禁联想起了一名可敬的朋友来――他就是《天葬》和《黄祸》的作者王力雄，我们都叫他“雄哥”。从某种意义上说，西藏就是潘多拉，王力雄就是杰克，而唯色就是纳特莉。<br />
<br />
前几天，与一名多年没有见面的中学同学一起吃饭。他在政府部门做一名循规蹈矩的公务员，在家中堪称好丈夫和好父亲，依旧保持着同窗时代善良温和的性格。不知怎么地，我们谈到了西藏问题，他立即愤愤然地说：“中央将藏人宠坏了，这些年我们给了他们多少钱啊，还把铁路修都通了。为了表示民族平等，在西藏的各级党政机关，中央都按照很高的比例安排藏人担任要职，即便藏人的能力比汉人差一些，因为有民族身份上的优势，升迁的机会都要多得多，在西藏甚至造成了一种‘逆向种族歧视’的局面。”他说，他感到难以理解的是：“我们对他们百般迁就，他们为什么还不满意，还要搞暴动，还要呼应赖喇嘛的煽动呢？”这位同学并非坏人，但他的看法绝对是一个坏的看法。而这种看法，亦非孤立的个案。据我所知，大部分的汉人，在中国占人口绝对多数的汉人，无论是富人，还是穷人，无论是有权者，还是无权者，大都是这样想的。人们并不觉得这种看法有什么不对。他们不知道，人活着，不单单靠食物，对于某些人来说，自由比食物和生命更加重要，比如《勇敢的心》中的苏格兰英雄华莱士，《角斗士》中的古罗马将军马西莫斯，《三百壮士》中的斯巴达国王列奥等等，对于他们来说，“不自由，毋宁死”。<br />
<br />
《阿凡达》中殖民公司的主管和雇佣军的头目，思考问题的方式跟我的这名同学一模一样。“我们给你们修学校，修公路，修医院，你们还有什么不满意的呢？”这些傲慢的殖民者认为，野蛮人就应当主动给文明人让路，因为历史是一个线性进化的过程，历史当然遵循一套“物竞天择，适者生存”的“显规则”。难道仅仅为了尊重野蛮人的宗教信仰，就要让文明人牺牲开采稀有矿石的机会吗？土地和森林，不是永远属于野蛮人的，尽管你们祖祖辈辈生活在这里，但如果你们没有能力“有效”地利用这些资源，那么由我们来帮助你们“利用”，难道不是一件天经地义的事情吗？“天下本无姓，唯有力者得之。”如果你们胆敢抗拒“现代化”，那么只好兵戎相见了。于是，“天人合一”的纳索部落的末日便降临了。那个面目狰狞的雇佣军上校宣布说，“被屠杀”是你们的宿命――“我们”比“你们”先进，就是“我们”屠杀“你们”的唯一的理由。<br />
<br />
二零一零年的全国研究生考试政治科目中，第十五道选择题是：二零零九年三月二十八日西藏自治区各族各界万余人身着节日盛装在拉萨布达拉宫广场隆重集会。热烈庆祝：A．西藏自治区和平解放五十八周年；B．西藏自治区成立四十四周年；C．西藏自治区九届人大二次会议召开；D．首个西藏百万农奴解放纪念日。所谓的“正确答案”是D。我不知道究竟有多少考生答“对”了这个题目，更不知道有多少学生从心底里认为这个答案是“正确”的。但是，《阿凡达》所给出的“正确答案”显然不是D，达赖喇嘛、王力雄和唯色所给出的“正确答案”也不是D。当所有的考生都被迫接受一个所谓的“正确答案”的时候，当所有的考生都相信西藏人民“身着盛装”、“隆重庆祝”的时候，每一个考生都潜在地成了《阿凡达》中的那些杀气腾腾的士兵。为了矿石这种赤裸裸的财富而杀人，与为了“统一”这一抽象的理念而杀人，表面上看，前者粗鄙，后者崇高，但实际上两者并没有本质的差别。<br />
<br />
多年以前，当王力雄开始关注西藏问题的时候，他还是一名对体制寄予厚望的改革派知识分子。他希望通过一种独特的“行走中国”的方式，完成一份对“改革大业”有所帮助的调查报告。由此，在青海藏区的黄河源头，他独自乘坐用汽车内胎扎捆的筏子漂流了一千二百多公里，搜集了大量沿途的人文地理的第一手资料。王力雄的身上浸染了八十年代理想主义的激情，更有中国古代士大夫“先天下之忧而忧，后天下之乐而乐”的自我期许。西藏的“不稳定”让他隐约感到不安，他的梦想是，以自己的智慧帮助中枢制定一套更加“合理”的西藏政策。哪个中国知识分子从未有过这种“帝王师”的情结呢？《阿凡达》中刚开始接受“重大任务”的杰克，何尝不是怀有《无间道》中那个到黑帮卧底的警探那样“舍我其谁”的使命感呢？那个时期，王力雄的写作和思想，与当时绝大多数中国知识分子的写作与思考一样，都自我设定了一个潜在的阅读者――“今上”。<br />
<br />
然而，西藏血淋淋的真相带给王力雄一次又一次的震撼，一九八九年天安门的枪声让他对共产党体制有了深刻反思，此后赴新疆调研期间被秘密警察抓捕乃至以自杀抗争的经历，更是让他如同杰克那样，毛虫化蝶、凤凰涅磐。从此，王力雄不再对“说服”中共当局或者中共当局虚心“纳谏”抱有任何不切实际的幻想，他在肉体和精神上都成了一名彻底的“民间知识分子”。二零零二年十二月，王力雄发起了对四川藏区阿安扎西活佛案的签名请愿活动，呼吁当局公正审理这一中国版本的“国会纵火案”。我是这份文件的签名者之一，这也是我首次参与一份“高度敏感”的公开信的签名。此后，我与王力雄的名字，经常同时出现在若干份与人权和宗教信仰自由有关的文件当中。也正是这封公开信，让我和我身边的很多独立知识分子，开始了对西藏问题的关注。一旦关切西藏问题，我才对自己多年以来对西藏的视而不见感到无比愧疚。在此意义上，中共多年来在西藏的罪恶，并非与我毫无关系。二零零八年，当西藏再次发声流血事件之后，王力雄发起了一份《中国部分知识分子关于处理西藏局势的十二点意见》的联署签名书，我毫不犹豫地成了第八个签名者。在我签署自己的名字的时候，不禁想起了马丁&#8226;路德&#8226;金的一句名言：“造成我们时代最大罪恶的是大多数人的袖手旁观，而不只是少数人的残忍行为。”<br />
<br />
王力雄对西藏的同情与支持，还让他收获了唯色的爱情。如同《阿凡达》中的杰克一样，能够让一个真正的男人奋起捍卫的东西，除了正义，便是爱情了。如果正义与爱情重叠在一起，那不就是人生中最美好的事情吗？在纳索部落生活的美仑美奂的森林里，杰克找到了地上的骏马和天上的大雕，脱离了轮椅的束缚，体验到了飞翔的感觉；在纳索部落生活的宛若天堂的森林里，杰克拜公主纳特莉为师，学习如何在丛林里生活，当两人各自驾驶着大雕在一片纯净的天空中飞翔的时候，他们比《射雕英雄传》中的郭靖与黄蓉不知要幸福多少倍呢。是的，当你爱一个民族的时候，你首先便是爱上其中一个具象人。每当我遇到那些对西藏问题指手划脚的“爱国贼”的时候，我通常会反问他们说：你认识某一个西藏人吗，你有一个藏族朋友吗？如果他的回答是“没有”，我便拒绝与他继续讨论下去，尽管他有可能对西藏的农奴制和人皮面具的“知识”如数家珍。若无情无义无爱，再多的知识又有什么用处呢？<br />
<br />
杰克因为爱纳特莉而爱潘多拉，王力雄因爱唯色而爱西藏，他们的爱情堪称“惊天地、泣鬼神”。唯色，更准确地说，她的藏文名字是“次仁唯色”，她没有纳特莉公主那样“羚羊挂角，无迹可寻”的好身手，却有像纳特莉公主那样的坚忍不拔的心志。她没有弓箭，只有一支笔。但仅仅是这支笔，就足以让那些武装到牙齿的侵略者感到害怕了。他们不发给她护照，不允许她出国访问；他们在她家的楼下安装摄影机，监视她的一举一动。他们的屠杀从来就没有停止过，从天安门到藏区，杀人成了家常便饭。杀人之后，便是铺天盖地的谎言，便是无孔不入的洗脑术，弱小如一株青稞的唯色挺身而出，从这层层累积的污血和淤泥之中，挖出一个可供呼吸视听的小孔。唯色所做的，就是恢复真相的工作，这是另外一个战线上的战斗，正如王力雄所说：“专制权力的虚假宣传和信息封锁，使得多数中国民众对西藏真相难以了解，也对达赖喇嘛的中间道路无法知晓。这是西藏问题长期无法解决的主要障碍。消除这种障碍，应当是中国知识分子的使命，因为最大的知识不是别的，正是真相。”为了揭示真相，她付出了太多、太大的代价，失去工作，乃至失去个人的隐私与自由。从秋瑾到刘和珍，从张志新到林昭，从丁子霖到唯色，这片土地上有过多少让须眉折腰的女性啊，如鲁迅所说，“但看那干练坚决，百折不回的气概，曾经屡次为之感叹”，她们如同《阿凡达》中那些漂浮在空中的精灵水母，柔弱到了极致，亦刚强到了极致，“足为中国女子的勇毅，虽遭阴谋秘计，压抑至数千年，而终于没有消亡的明证”。<br />
<br />
王力雄和唯色的结合，宛若天作之合，这段美妙的姻缘，似乎预示着汉藏关系的未来的微茫而温暖的希望。既然杰克和纳特莉可以携手制止来自地球的殖民者的为所欲为，为潘多拉创建美好的和平；那么，为什么王力雄和唯色不能以一种“牛犊顶橡树”的勇气和智慧，为汉藏这两个相邻生活上千年的民族提供一个和谐共生的愿景呢？二零零九年，达赖喇嘛亲自将“真理之光”奖颁发给王力雄，王力雄在答谢词中说：“专制造成的民族仇恨，反过来成为专制者拒绝民主的理由，而且得到受大汉族主义蛊惑的国民支持。这种绑架者与人质共生死的逻辑，是中国走向民主的一个难解之结。……超越这种困境，必须从推动民族间的民间对话开始。只有各族人民化解仇恨，实现团结，才能驳倒专制者以民族冲突而拒绝中国民主的理由。”电影毕竟是电影，电影中的杰克是如此英勇神武，所以唯色才在推特上幽默地跟我说，王力雄哪有杰克那么英勇神武啊！然而，对于手无寸铁的王力雄和达赖喇嘛来说，他们拥有智慧、道义、耐心和韧性，这智慧、道义、耐心和韧性是从“幼吾幼以及人之幼，老吾老以及人之老”的中原文化传统中而来，是从“舍身饲虎”的佛教文化传统中而来，也是从马丁&#8226;路德&#8226;金和图图大主教的非暴力抵抗的经验中而来。<br />
<br />
上帝创造的“天父世界”不应当是一个弱肉强食的世界，不应当是一个道德相对主义的世界。美国保守主义思想家列奥&#8226;斯特劳斯说过：“如果道德是相对的，那么食人只是口味问题。”地球人在潘多拉星球上的所作所为是恶，中共在西藏的所作所为是恶，这既是一个事实判断，也是一个道德判断。只有作出了这样的判断之后，我们才能起而言、才能起而行。在现实生活中，我们的反抗不可能像《阿凡达》中的杰克和纳特莉那样，得到大雕和怪兽的帮助。刘晓波博士曾经说过，跟无恶不作的统治者相比，除了道义力量之外，我们还有什么呢？我也记得二零零二年王力雄在领取独立中文笔会第一届自由写作奖的时候说过的一段话：“也许犬儒主义者们不会接受这里所谈的正义、良心等说法，他们最擅长的是质疑谁有资格去判定何为正义何为良心。对此我至少可以这样回答，只要专制权力依然横行于社会，就说明正义肯定尚未实现，只要世间还有苦难者的哭泣，谁有无良心就可以一目了然。”在这个意义上，此刻，我们都是藏族人；在这个意义上，只要藏族人一天不得自由，我们便是藏族人。<br />
<br />
二零一零年一月二十五日、二十六日<br />
四川成都</div>

							
							
							
							
