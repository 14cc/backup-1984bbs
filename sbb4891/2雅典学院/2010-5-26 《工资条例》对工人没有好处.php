													<h2>《工资条例》对工人没有好处</h2>
						
						
																											<div id="postmessage_640636" class="t_msgfont">铅笔经济研究社 周克成 <br />
<br />
媒体报道称，劳动和社会保障部起草修订的《工资条例》将在年内出台，工资协商制度、同工同酬等保障劳动者权益的条款将被纳入其中。很多人对此寄以厚望，认为工人境况可能由此得到改善。这实在是一种误解。这些所谓劳工权利，有的不需要特别立法保障，有的则根本不是工人应有的权利。如果强制推行类似制度，对工人其实毫无益处。 <br />
<br />
第一，“集体协商”很多余。从业者工资收入的高低和他们是否以集体协商的形式约定工资无关，而只和这个行业的劳动力稀缺情况有关。如果某些工作人人能干，那再怎么集体协商，这个行业的从业者收入也不会很高。反过来，如果人才稀缺，那哪怕没有什么集体谈判，这些人的收入也会很高。 <br />
<br />
比如金融行业的从业者收入就比建筑工人的收入高得多，但他们的高收入是集体谈判带来的吗？显然不是。正好相反，在现实中，建筑工人联合起来谈判工资的情况更常见，可是他们的收入还是更低。而这这是因为能做建筑工人的人相对较多，而他们之间的竞争激烈程度也更大而已。 <br />
<br />
第二，“同工同酬”并不合理。对同工同酬我们可以从两方面来看。一方面，要求严格的同工同酬是合理的吗？不合理。即使是在同样的岗位上从事同样的工作，不同的人生产力也不一样，因而不宜同工同酬；或不同的人生产周期不一样，如女人需要有产假、实习生不能长期按时工作等，因而也不宜要求同工同酬。 <br />
<br />
另一方面，要求不严格的同工同酬有必要吗？没必要。现在有一种解释是说，所谓的同工同酬并不是要求一样多的工资，而是“允许在同一工资区间内进行浮动”，就是说这并不是一个硬性规定，但既然这个条款不能对企业起到真正的约束作用，那就是多余的，就只能给企业和劳工徒增纠纷隐患，而对改善工人利益毫无益处。 <br />
<br />
第三，“组织权”无需特别立法授权。法律界有一句经典谚语：法无禁止即可为。在劳工市场上也是如此，如果没有什么法律禁止劳工结社，那劳工就天然具有这种权利；如果有法律禁止劳工结社，那就废除这些法律即可。而不必专门立法规定劳工有什么组织权。 <br />
<br />
第四，“谈判权”也早就存在。任何人寻找工作，工资是完全有雇主决定的吗？当然不是，每个人工作之前都会先和雇主约定劳动报酬的支付方式和水平。也就是说每个人都已经拥有“谈判权”，何须额外立法保障？ <br />
<br />
第五，“罢工权”并不是劳工应有的权利。看惯了国外罢工的新闻，学者也喊得够多了，人们就以为罢工是工人应有的权利。实际上这是错误的，是对雇主很不公平的。和别人协商好了工作内容和报酬水平，但又希望自己可以随时不去上班，这是一种无赖行为，而不是什么劳工权利。 <br />
<br />
人们普遍认为劳动者和雇主是一种竞争关系，但这真是天大的误解。实际上，劳动者和雇主是一种合作关系，他们是一方出资，一方出力，一起合作生产产品并推向市场，然后一起来瓜分其中市场收益。当然在分配收益的时候可能有的得到多一些，有的得到少一些，这要视他们不同的贡献程度和之前的风险分担比例而定。但归根到底，他们还是一种合作关系，只有一起把蛋糕做大了，他们才能得到更高的收入。如果外人强行介入，说一定要给某一方多少多少蛋糕，那就只能破坏他们的合作关系，从而从长远上减少他们双方的收入。</div>

							
							
							
							
