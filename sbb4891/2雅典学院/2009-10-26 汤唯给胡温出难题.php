<br />
我们是一些电影、法律工作者和其他行业的劳动者，首先对你们再次当选国家主席和国务院总理表示祝贺，藉此机会，我们就两会期间国家广播电影电视总局下令禁止播放有汤唯出演的影视广告一事向你们提出建议。<br />
<br />
3月18日，《新京报》和新浪网等多家媒体报道广电总局的官员对媒体的陈述，其中言明此次发布禁令是因汤唯参加了由上海电影制片厂和香港、台湾电影公司联合摄制的电影《色戒》。<br />
<br />
《色戒》上映之后，两岸三地的广大观众和文化研究者对它的艺术手法、历史背景、人物形象塑造、情爱镜头的处理和主题思想进行了热烈的讨论，其中不乏大相径庭乃至针锋相对之处。这种讨论加深了社会公众对影片艺术品味和思想主题的认识。在此之前，我们未见到政府部门对该片上映以及讨论有明确行政性干预，这颇为明智，是世界各国对待文学艺术争议的惯常态度，也是符合你们多次倡导的法制和宪政精神。现在，广电总局却实施了对汤唯出演的影视广告作品的禁止令，这引发了许多艺术以外的不解和热烈争议。<br />
<br />
即使宣传或者政府部门认为本片确有不妥，认为该片会对公众或某种政府主导的文化价值观造成严重危害，因而决定加以处理；那也只能处理决定该片能否公映的责任人。因为，在多数情况下，国营大电影制片厂领导的政策水平、艺术水准被认为要比青年女演员汤唯高，在判定该片是否符合宣传部门希望推广的意识形态和历史定论方面，国营电影厂领导和主管部门领导当然要比汤唯更为明白；而在决定该片是否允许被全国观众观赏、讨论方面，则完全是这些政府管理人员拥有决定权，汤唯则丝毫没有任何权力和机会置喙其中。当然，对类似文学艺术争议政府一定要出面加以处理，如果能有所讨论、公开给出理由就更好，或许有助于达到这类决定希望起到的作用。<br />
