													<h2>党史 有些就是不能对老百姓讲</h2>
						
						
																											<div id="postmessage_856026" class="t_msgfont">中央党校党史教研部学习习近平讲话座谈会纪要<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; 2010年7月24日<br />
<br />
主持人 ：石副校长<br />
<br />
参加人：罗，王，李，冯，任，郭，韩，王，胡，谢，程，陈，柳，曹等教授，博导14人。<br />
<br />
主持人：我校校长，政治局常委，国家副主席习近平同志&nbsp;&nbsp;月&nbsp;&nbsp;日在我校的讲话极为重要，为我们今后的党史教学与研究 ，宣传与出版工作指明了方向，制定了原则，指出了道路。今后，我们一定要明确方向，遵守原则，走上正路。大家知道，今年来，我们在如何对待中共党史问题上 出现了一些混乱，主要问题是是否要把我党历史上的一些有争议的事情搞清楚，有争议的问题怎样评价，教学中讲不讲，对社会上是否透露 ，是否出版，是否允许我们的教员接触机密档案材料，是否允许采访当事人，能否作为科研课题，算不算科研成果，是不是与个人职称待遇挂钩等等。我看习校长的 讲话把这些问题基本上都解决了。我们的一切工作就是要坚定不移地维护党的利益，维护党的领导地位，要做到这两点，我们要做到凡是有损我党光辉形象的事坚决 不想，坚决不说，坚决不写，坚决不做。下面请同志们发言。<br />
<br />
罗：我是坚决拥护习近平同志的讲话精神的，特别是在我们党校，不是一般的党校，是中央党校。我们的学员不是一般的学员，都是我党的中坚骨干分子。我 们的任务就是让这些学员更中坚，更骨干，让他们相信我们党从成立那天开始就是三个代表，近八十年来大方向一贯正确，这一点绝不能含糊。如果有学员问到历史 上的某些问题，特别是一些负面东西，我们怎样回答？我想，就讲是某些人的失误，是一时一事，我们党也有个学习过程。还要强调个别的机会主义分子，野心家也 会干扰我党的大方向，但我们战胜了他们，更说明我党的伟大光荣正确。<br />
<br />
王：我们党史中的一些史料就是不能对老百姓公开。为什么？不是说群众有知情权吗？对，那是一方面。另一方面，我们党也有保密权。哪个国家，哪个党都 有不能公开的秘密，而且不许探听。个人有隐私权，党也有隐私权，不能侵权。我们党的一些隐私如果让老百姓知道了，肯定会造成思想上的混乱，会怀疑我们党执 政的合法性，那就乱了。一乱，社会怎能和谐，经济怎能发展，老百姓生活怎能提高？最明显的例子就是89年那场风波，我们就是不能再讲了，因为说不清，非要 讲就是揭伤疤，更疼，还要流血。我们不讲，你们看，这二十年发展得多好。父母不讲，学校不讲，青年人不知道，这对大家都好。还有，59，60，61年三年 困难时期，你要是正式告诉老百姓我党执政时期，三年里饿死三千八百万人，成千上万的村庄成了无人村，哪还了得了！比日本人杀中国人还多，还省事，还省时， 还省钱，还不用牺牲自己人。老百姓听了这些非反了不行。所以我们讲党史要有底线，越过底线就犯规，就要受惩罚。习近平同志的意思也是这个。<br />
<br />
任：我就是不明白为什么有些人非得要将历史上的事情搞得清清楚楚，加上自己的结论，还非得要出版，要讲课。我们说要向前看，不要再纠缠在历史旧账上 了。不说，对大家都有好处。中国人聪明，表现之一就是认识到一个真理“难得糊涂”。在座的各位领导，博导，教授，我们是党校党史教学科研人员，我们的利益 与党的利益紧密相连，一荣俱荣，一损俱损。我们都有了精神方面的财富，有高级职称，有社会地位。我们的物质财富也比二十年前大幅提高，住房，汽车，家电， 假期，医疗保障，有存款，丰衣足食，离退休保障，不少人给孩子，甚至孙子的房子都准备好了，你死了单位还出钱开追悼会。我也出过国，到过发达国家，我们在 座的诸位的物质生活不比人家差。这些都是因为有了我们的党，我们又与党紧密相连，这就叫鱼水情。不就是不让你说点历史上的事吗，权衡一下利弊，还是不说为 佳。周总理曾经说过，党内的事，有些到死都不能说。你要是非讲党史上的负面事情，那才叫般起石头砸自己的脚，而且把群众的思想搞乱了。一乱，什么都完了， 像文革那样。所以我们不能随便讲，这也是符合广大群众的利益。本来群众也没非要知道一些事，一些党史实情，咱们也不要故作多情非讲不可。<br />
<br />
郭：我就仅举一个例子，抗日战争。我们几十年来，几代人都知道，都相信我党1934年进行了长征，爬雪山过草地，目的就是北上抗日。到达陕北后成为 抗日战争的中流砥柱，领导全国人民抗战八年，打败了日本侵略者。我们也一直谴责国民党政府，像京剧沙家浜里说的“国民党反动派卖国求荣，不抵抗，假抗日， 真反共，委曲求全，为虎作伥”。这些已经深入人心，全国人民也都相信这个宣传。可是现在有的党史教研人员不知通过谁的关系看到了我党1937年陕北洛川会 议决定，当时我党制定了抗战时期的战略方针，即不与日军做大规模正面冲突，保存实力，积蓄力量，扩充队伍，让国民党去和日本人打，两败俱伤，待日本人撤离 后，共产党可趁机夺取全国政权。毛主席在上世纪七十年代接见来华访问的日本首相时清楚地说明了两点，第一，没有日本侵华，中共肯定会被国民党消灭了，你们 一来，国民党只好先放过我们，去打日本人，我们趁机坐大并夺取了政权。第二，我们要感谢日本侵华，没有日本侵华就没有中共的今天，所以我们的感谢是真诚 的。大家说说，这两件事能随便就公开承认，让老百姓知道吗？否则老百姓就会说你共产党真会耍阴谋，骗了全国人民几十年，几代人，你中共还有很多事瞒着我们 吧？所以，不该讲的一定不能讲，不该承认的一定不能承认。<br />
<br />
韩：有人说从1949年到1979年这三十年党史非常不好讲。我承认这个。因为要宣传我党的伟大光荣正确，可是这段时期可大肆宣传的好事不多。但是 只要我们认真学习，深刻领会习校长的讲话精神，这事也不难做到。我们不是干部，没有党政大权，你有了权，别人不信也得信，不服也得服。我们是教员，是导 师，要以理服人，首先要自信。我们经历过那么多的大起大落，没被淘汰，随风就势，与时俱进，能做到今天的教授，博导，说明我们有能力适应新环境，完成新任 务，能写出有说服力的文章和讲义。我们的另一个优势是学员都是受党教育多年的干部中的精华，他们懂纪律，懂得与中央保持一致，而且是思想上的一致。他们不 会往歪门邪道上想，我们讲什么他们就听什么，不会给我们出难题。习校长要我们讲党史要讲主流，49至79年这三十年的主流是什么？就是我们坚持了社会主义 道路，坚持了党的领导，坚持了马列主义，全国人民团结一心听党的话，没有分裂，克服了无法想象的困难，为后三十年的高速发展打下了基础。按照这个主线讲就 没问题，谁也驳不倒。具体怎样讲，大家都有不寻常大脑，我相信都能讲好。<br />
<br />
胡：前两年我看到一篇攻击我党的文章，说我的夺取政权靠的是枪杆子和笔杆子，我党当政后维持政权靠的还是这两杆子。文章意思是说，一是靠枪杆子武力 镇压民众自发运动，二是用笔杆子造谣，歪曲，篡改，掩盖历史真相。哪个国家维护政权不靠枪杆子？西方国家警察都带枪，用警察代替军队 维护政权。我国警察不带枪，形势危急时动用武警或军队，实质上都是一样的。笔杆子这一条，我觉得现在比枪杆子还重要。我国与西方国家不同，人家多年来养成 了习惯，不随意相信政客们的讲话，不随意相信各种出版物的观点，大家随便讲。你把执政党讲得再坏，人家选上来的，还是下不了台。你把在野党说得再好，大家 不选你，你也上不了台。我们的国情就不同了。建国后，不管我党说什么，老百姓都认为是真理，谁要说党出了错，全国人民都要共讨之，共诛之。这就是我们宣传 的功绩，是笔杆子的作用，这个传统不能丢。现在呢，老百姓还是相信，或者叫轻信，信什么？专信那些对我党不利的消息，一传十，十传百，一呼百应，然后闹 事。在这种情况下，不用政府公布一些内情，只要有个教授，有个名人举出一点负面例子，大家就相信，他说一句话造成的恶劣影响我们说一百句也挽回不了。在这 种国情下，就不能给老百姓讲什么我党历史上的，从未公开承认的所谓事情了。你承认一件，他们又拿出十件来问你，你就下不来台了。俗话说好事不出门，坏事传 千里，这样讲下去没个头了，最后变成罄竹难书了。因此我举双手赞成习校长的讲话，他的讲话精神就是该讲就讲，不该讲的就不能讲。<br />
<br />
谢：讲党史离不开党的指导思想。我们党的指导思想是什么？我们一直讲是马列主义，毛泽东思想，这是党的思想的主线。1949年以前好说，不管别人怎 样质疑，反正我们胜利了，掌握了政权。在错误路线指导下是不可能胜利的，不可能歪打正着，这叫反证法，谁也驳不倒。解放后的前三十年怎样讲？有的教员提出 是在错误思想指导下搞革命，搞建设。我看解放初期的镇反，肃反主流是正确的，就是多杀了，多关了，多劳教了不少人。可是没点儿红色恐怖，我们的政权就不会 稳定。包括反右，反右倾，四清，虽然搞错了，但是这些运动强化了群众头脑中党是神圣不可侵犯的观念，强化了谁反对党的领导谁就下地狱的观念。我们的党经过 十年文革仍牢牢掌握政权不能说和这些运动没关系。直到今天，你要说某个人反党，保证让他吓得睡不着觉。那些搞所谓民主运动的人，提倡竞选的人也不敢公开反 党。上面说的是国内，下面说说国际方面的事。79年以前我党指导思想的一个重要方面就是在国际上反对修正主义，批判以苏联为首的修正主义集团。我党在毛主 席亲自挂帅下发表了九篇经典文章，即“九评”。它们全面，深刻，准确地反映了当时我党的指导思想。我们批修，实际上也是给国内干部群众看呢，没人敢反驳， 老百姓接受和继承了这些思想，对稳定文革后的局势起了重大作用。可是你现在把“九评”发给老百姓看就麻烦了，老百姓就会说九评都是胡言乱语，你中共做的比 苏修还修一百倍。白纸黑字，咱们是有口难辩，跳进黄河洗不清。现在的中年人，青年人对这些基本不清楚，不知道，也不感兴趣。毛主席说“人贵有自知之明”， 我们不要引火烧身，这也是习校长讲话的深层含义。从79年到现在这后三十年就好说了。我们可以理直气壮地讲，我党是在正确思想指导下建设有中国特色的社会 主义。邓小平理论，江泽民的三个代表，胡锦涛的科学发展观都是对马列主义毛泽东思想的发展，取得了举世瞩目的成就。这样再反过来证明我党的伟大正确。全世 界都对我们这三十年都心服口服，说得不太谦虚点儿，全世界都在研究我们，来取经，来朝圣。你国外再把我们说得这不好，那不好，你别来呀，有种的把你们的发 展速度和我们的比一比。我们能有今天这种百凤朝阳的局面，不能不说是我党宣传战略战术的伟大胜利，一定要继承下去。<br />
<br />
程：说到毛泽东思想，特别是49年至79年这段时期的毛泽东思想怎么讲，我们先看一下大政方针。2009年国庆，我们的游行队伍中就有毛泽东思想方 阵，我们党和政府的中枢神经所在地中南海大门外墙上的大标语仍是“战无不胜的毛泽东思想万岁”，天安门城楼正中间悬挂的还是毛主席的巨幅画像。这就明确地 告诉我们，第一，不能否定毛泽东，他还是我党的伟大领袖。第二，毛泽东思想仍然是战无不胜的，还是我党的指导方针。我们以前总结建国后的毛泽东思想就是无 产阶级专政下继续革命的理论，就是以阶级斗争为纲，反对资本主义复辟，防修反修，警惕隐藏在党内的修正主义分子篡党夺权。现在这些全都不能再讲了。出了什 么问题？我们的学员不会提出这样的问题，他们身份特殊，前途无量。到是我们的教研人员中有人提出这类问题，说什么合作化，公私合营，反右，三面红旗，中苏 论战，四清，文化大革命都是毛泽东发动的，都是毛泽东思想的实践，结果都是战无不败。怎样对待这个问题，我想，第一，不在校内教学中讲。第二，受聘到校外 讲课作报告时也不主动讲。第三，如果真有某些不识时务的人提这类问题，不用紧张，前些日子就有人拐弯抹角提了这个问题，我们的曹教授很巧妙地回答了这个问 题。我建议组织几个人，研究一下，从理论上解决它，也算我们的科研成果，将来遇到挑战就可应付自如了，省得闹个脸红脖子粗，下不了台。<br />
<br />
陈：说党史又离不开现状，现状最大的挑战就是所谓的腐败。老百姓一说到党，不说怎样伟大，不说怎样感谢党，张口就说怎样腐败。什么病入膏肓呀，什么 盘根错节呀，什么前腐后继呀，中国文字中的负面词汇都说遍了还嫌不过瘾，还要古今结合，中外结合，音韵结合，编顺口溜，创造新名词诋毁我们的党，唯恐天下 不乱。 从中央最高层都很明确地提出反腐的问题，是关系到我党生死存亡的问题。我认为是说到要害了。当然，像有病不治，特别是慢性病，或治标不治本，人也会不死， 拖个十年八年没问题。党内腐败是什么病？慢性病，急性病，还是癌症？我想反正不是绝症，但不治的话，它又蔓延得非常快，保守治疗吧，又根除不了，动大手术 又承受不了，这是我党在新时期面临的新课题，非常具有挑战性。要说严惩腐败分子，这个好说，随便找几个干部，肯定能查出问题来，判他个十年八年没问题。但 是不从根源上彻底清除，那还是野火烧不尽，春风吹又生。一说到根源，有些人就两眼放光，说根源就是没有民主自由，民主自由就可保证人民有选择权，有批评和 监督权。有批评和监督就可发现腐败根源，有选择权人民就可以清除它。这些是中听不中用，还是美国那套资产阶级民主自由，现在由于时代潮流变了，我们不这样 提了，但并不等于我们可以接受。我们党和国家领导人贾庆林，李长春，贺国强都一再强调中国绝对不能搞西方民主自由，不能搞三权分立，因为历史赋予中国共产 党为中国各族人民利益的唯一代表，这个地位和责任不允许任何人，任何政党挑战。不太恰当的比喻就是“君权神授”。不坚持中共的领导，中国就会乱，中国人民 就会吃二遍苦，遭二茬罪。当然，领导人的讲话都没说清楚西方民主自由有什么不好，领导人一般来说都不会详细论述，这就是为什么我们党校能存在的理由。这详 细论述的光荣任务就由我们党校教研人员来承当了。<br />
<br />
曹：中央领导人提出大政方针，我们就要详细论述，要让各级领导干部不但执行，思想上也要有明确认识，我们中央党校就是干这个的。为什么只有中央常委 才能当我们的校长？因为我们的学员就是将来，有些现在就是各级领导干部，他们就听中央的，校长直接代表中央，他的讲话才具有绝对权威。中国历史上只有黄埔 军校，延安抗大才可相提并论。我们的学员是来学理论的，实际上就是来接受政治思想教育的，不是来做学术研究的，给他们发个文凭也是为了回去后好开展工作。 学员们都是接班人，管好他们的思想，走在正路上，这是我们的责任。要做好他们的工作，首先从我们做起，要深刻领会习校长的讲话精神，管好自己的嘴巴，不该 说的就不要说。<br />
<br />
柳：我觉得给学员讲腐败问题时要非常谨慎。老百姓说我党腐败已经说得都不想再说了，我们的学员听得都不想再听了。在以经济为中心的今天，实际上就是 以创造财富，积累资本，再创造，再积累为中心。对个人和家庭来说，就是要获取更多的金钱，更多的财产，更多的享受。而且大家都有这个合法权利。捞钱最容 易，最快的途径就是勤劳，智慧，机遇，胆识与权力相结合。我党是执政党，各级官员都是聪明人，有魄力，要想发财致富太有条件了。官员们经常明里从政，暗里 经商，或让老婆孩子经商发财，更省事的根本不用经商，商人自动就把钱送上来，各方利益安排好了。这就是为什么今天在中国抓腐败分子很容易的原因，都不用警 察，用不着侦察员，老百姓随时都可拿出证据说谁是腐败官员，而且一挖一大堆，一抓一大片。我们党目前内部掌握的政策就是抓典型，杀一儆百，不能有一个查一 个，否则就乱了。全都查，全都抓，再有目前十倍的警察，法院，律师也不够用。再说目前的警察，法院，律师们都干净吗？都查都抓，各级党政机构就没法工作 了，瘫痪了，像文革那样了。俗话说“法不治众”嘛。我们对学员讲，当然要强调腐败分子是极少数，绝大部分干部是廉洁奉公的，是主流。这是习校长的精神。实 际上腐败也是相对的，二十年前你贪污五千块钱就是犯罪，现在你贪污十万块都不算个屁了。<br />
<br />
主持人：还有人想发言，因为时间关系，座谈会只能到此为止了。今天，我非常高兴地看到，我们中央党校党史教学部的同志们党性特别强，水平特别高，而 且完全相信同志们一定会把中央的精神准确地融汇贯通到教学科研中去。我还看到，我们的教员和教研环境就是与校外的不同，在领会中央精神方面具有巨大的优 势，而且这一优势会不断地延续下去的。<br />
<br />
散会之前我想提醒大家一下：今天这个座谈会是内部举行的，我看到有的同志做了记录，请不要外传。因为我校不同于社会上的一般院校，观点不尽相同，免得出现不必要，不健康的争论，有损社会和谐。<br />
<br />
谢谢大家，散会。</div>

							
							
							
							
