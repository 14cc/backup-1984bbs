<br />
由于党派林立加上频繁的合纵连横，泰国的政局一直不够稳定。出于对传统政治家的不满，国民转而支持新兴的政治力量。<br />
<br />
1998年，他信&#8226;西纳瓦创建了名称有些拗口的泰国人爱泰国党，简称泰爱泰党。他信是一位成功的商人，电信业巨头。1994年，他信开始从政，曾任外交部长、两届内阁副总理等职。<br />
<br />
在2001年大选中，泰爱泰党获得胜利，他信出任总理。在泰国历史上，他信实现了民选政府第一次坐满任期。2005年，他信再次连任，并且实现了第一次由一个政党组建政府。他信执政期间，泰国的经济克服东南亚金融危机的影响，获得了较快的发展。另外，他采取了不少扶助贫民的政策，受到农民的拥戴。<br />
<br />
于此同时，经济发展给予城市居民和中产阶级没有带来多少益处，有些人的生活状况甚至有所下降。听到种种关于腐败的指控和传闻，很多人愤愤不平。<br />
<br />
他信也有不少问题。担任总理之前，法院就对他信是否偷税漏税的指控进行表决，最后以8比7的微弱多数否决。2006年1月，他信家族向新加坡淡马锡控股公司出售泰国最大电信公司西那瓦集团，成交价格为18．8亿美元。这一行动，被广泛地认为是以权谋私和逃税。<br />
<br />
他信出身华裔，精明强干、敢冒风险，又有些独断专行，自称为“成吉思汗型领导”。有人认为，这是“客家人的强硬作风”。在2005年大选中，反对派指控他信操纵选举，应该是有根据的。<br />
<br />
不论作为商人还是政客，都应该搞好各方面的关系。而在顺利的条件下，他信多少有些自我膨胀，与国王和军方的关系十分紧张。据泰国华人讲，国王在一个地区做了一个扶贫项目，他信不去回避，也要在那里开展类似的项目。另据报道，一次国王和王后接见官民，国王一直对在场的人微笑致意。当国王看到他信的时候，突然扭头去看别处了。<br />
<br />
应该说，有些事情不是他信的过错。但是，为了事业的顺利，必要的斡旋和让步也是不可少的。<br />
<br />
4. 2006年军事政变<br />
<br />
2005年他信连任以后，抗议活动不断发生。有趣的是，发动抗议的一个重要的领袖人物，就是1992年民主运动的领袖占隆&#8226;西芒。2006年2月，占隆和林明达组成人民民主联盟，坚定地与他信对抗。以前，两人曾经是他信的朋友。<br />
<br />
2006年09月19日，泰国陆军总司令颂提&#8226;汶雅叻格林领导军方发动政变，推翻了正在纽约参加联合国大会的他信总理。与过去一样，军方谴责前政府腐败，并且答应举行新的选举。<br />
<br />
5. 军事政变与竹篮打水<br />
<br />
2006年政变在开始的时候非常顺利。在曼谷，不但没有遭到抵抗，还受到了市民的欢呼，还有市民将玫瑰花献给政变军人。<br />
<br />
不过，政变的玫瑰色彩很快就消退了。不但联合国及西方各国表达了不满和谴责，国内的支持也有所减少。应该说，时代变了，政变当局已经不能完全左右政坛的运作。<br />
<br />
尽管政变当局解散了他信的泰爱泰党，泰爱泰党的成员组成了人民力量党，还是赢得了２００７年１２月泰国大选。2008年１月２8日，他信的盟友沙马当选新的总理。<br />
<br />
就这样，一切都回到了起点，军事政变成为竹篮打水。</div>

							
							
							
							
