																											<div id="postmessage_626942" class="t_msgfont">泰国动乱：民选、国王、军人三角的断裂<br />
刘松萝<br />
<br />
泰国的政治危机已经持续好几个月了。近来，局势日益恶化。2008年8月26日，泰国人民民主联盟的支持者占领了总理府。9月2日，支持政府和反对政府的两派民众发生流血冲突，造成至少3人死亡，20多人受伤。随后，泰国总理沙马宣布曼谷地区进入紧急状态。不过，紧急状态并没有能够平息事态。9月4日，沙马在内阁会议后宣布将举行公民投票，以决定现政府的命运。<br />
<br />
泰国的政治危机，宣告了泰国式民主，也就是民选加上国王和军人干政的模式已经失效，民选、国王、军人的三角关系已经断裂。<br />
<br />
1.回顾（1）：1973年，民主走向前台<br />
<br />
现代泰国的历史，是与军事政变分不开的。1932年 6月24日，暹罗（泰国）人民党领导了军事政变，君主立宪政体从此确立。遗憾的是，1932年政变不但推动了泰国的进步，也为频繁的军事政变打开了大门。<br />
<br />
1946年，现任国王普密蓬&#8226;阿杜德 ( Bhumibol Adulyadej ，意思是“土地的力量，无与伦比的能力”)即位。普密蓬国王是在他的兄长遇刺之后继任的。在即位的第二年，就遭遇了军事政变。国王在位60多年，经历了20次军事政变。<br />
<br />
上个世纪，泰国最有权势的军事强人是他侬&#8226;吉滴卡宗。从1957年政变以后，他侬一直是政坛的重要人物。1963年，他侬&#8226;吉滴卡宗出任总理，与巴博&#8226;乍鲁沙天组成军事独裁政府。当时正值越南战争，由于他侬和巴博支持美国的行动，得到了美国的认可。那时中国对他侬和巴博恨之入骨，将他们斥为他侬-巴博集团。<br />
<br />
1973年，民众的不满开始加剧。法政大学学生率先举行抗议活动，以后曼谷民众走上街头，要求结束军人统治。随后军政府实施了镇压，酿成流血事件。在这一关键时刻，国王宣布撤销对军政府的支持，他侬-巴博的统治倒台。<br />
<br />
经过1973年民主运动，泰国开始向民主政体转化。1976年，民主被军事政变打断了。不过，后来的军人统治已经无法排除国会的运作。<br />
<br />
2. 回顾（2）：1992年，民主制度的确立<br />
<br />
1988年，差猜&#8226;春哈旺被任命为总理。差猜出身军人，曾经在他侬-巴博政府任职。经过12年的军人统治之后，权力第一次落入民选政府手中。差猜致力于通过改革建立文官治国的制度，得罪了军人集团。同时，差猜政府由于在廉政方面有所欠缺，在民间的威望很差。<br />
<br />
1991年2月23日，泰国武装部队最高司令顺通&#8226;空颂蓬发动政变，推翻了差猜&#8226;春哈旺政府。政变军人主张反对腐败，并且许诺在大选以后还政于民。但是，1992年大选结束以后，没有当选为议员的陆军司令素金达&#8226;甲巴允被任命为总理，激起了民众的不满。<br />
<br />
1992年5月，抗议示威再度爆发。这次民主运动的领导者是退役将军占隆&#8226;西芒，我们记住这个名字，因为他在以后的政治运作中仍然起着主要的作用。素金达用武力镇压民众的抗议，造成数百人死亡的流血事件。<br />
<br />
面对危机，国王召见了总理素金达和反对派领袖占隆，要求他们克制。<br />
<br />
自1992年民主运动以后，民主制度在泰国确立了。以后虽然政府更迭仍频，但都是在民主程序下进行的。<br />
