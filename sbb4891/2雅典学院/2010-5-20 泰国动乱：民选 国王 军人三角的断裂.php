																											<div id="postmessage_626949" class="t_msgfont">10.思考：民选、国王、军人三角的断裂<br />
<br />
泰国的民主是不完善的。以往，由于国王的存在，部分地抵消了军事政变的消极后果，也让泰国在动荡的政局中保持相对的稳定。出于对泰国国王的良好印象，我一直相信国王不赞成军事政变。但是在2006年这一次，我不相信国王了。<br />
<br />
长期处于尊贵的地位，参加高度复杂的平衡游戏，不免会做出错误的判断，做出灾难性的决策。在游戏当中，还不免会把自己的地位和利益放在第一位。<br />
<br />
在2006年，军人和国王把游戏玩大了。在民主已经成为世界潮流，军事政变已经备受谴责的今天，军人轻率地发动政变，以后又无法控制局势，只能眼见旧政敌卷土重来。而国王以个人的好恶为重，默许或者听任军人发动并不成功的政变，极大地削弱王室的道德力量。<br />
<br />
如今泰国已经进入动乱之中，国王和军队却在不作为，以中立为名借用抗议者的力量攻击政敌。抗议者冲进总理府不仅是沙马总理的失败，民主体制的失败，也是国家的失败，包括王权的失败。对这种近乎叛乱和叛国的行为不加制止和惩戒，足见以往国王和军队，特别是军队干政的理由，比如国家利益和秩序是何等的虚妄。为此，国王和军队早晚会付出代价。<br />
<br />
应该说，民选、国王、军人的三角已经断裂。他信对政治平衡的打破负有一定的责任，而国王和军队更是火上浇油。<br />
<br />
最可怕的是，泰国已经违背了宽容、妥协和尊重惯例的传统，走上了政敌之间不共戴天的死路。<br />
<br />
11.思考：谁代表民主？<br />
<br />
关于这个问题，在《从巴基斯坦到泰国，军事政变的末路》中还要详述。这里，仅仅提出问题。<br />
<br />
总的来说，谁都不是民主的化身。但在具体问题上，是非还是存在的。<br />
<br />
那么，谁代表民主呢？<br />
<br />
从上个世纪70年代起，民主党一直是民主的倡导者。他们反对他信专权的时候，也还是民主的。但是，当他们为了权力支持军事政变，为了权力以“反对国王”的罪名陷害对手的时候，他们离开民主就太远了。<br />
<br />
在1992民主运动中，占隆&#8226;西芒将军是当之无愧的英雄。当他率领民众抗议的时候，子弹就在头上飞过。当他在2006年抗议他信的腐败时，尚可以有合理的解释。而占隆领导的人民民主联盟支持军事政变，并且在今年不接受民主选举的结果，醉心于街头政治和暴民政治的时候，就走向了自己的反面。人民民主联盟主张国会议席七成任命，三成选举，完全与民主背道而驰。<br />
<br />
至于炳&#8226;廷素拉暖在军人政权担任总理的时候，曾经促进过宪政的进展。而在2006年，他成为军事政变的策划者。<br />
<br />
他信阵营虽然缺乏道义上的力量，如今沙马总理的坚守，实际上就是对民主的坚守。<br />
<br />
因此，政治人物应该珍惜自己的过去，不要自己否定自己。根据个人的印象，我觉得泰国的政治人物过于看重个人的作用，而不管这种作用的结果是什么。<br />
<br />
12.结论<br />
<br />
- 民主需要妥协。他信&#8226;西纳瓦行事过于强硬，自己又以权谋私，是不可取的。<br />
<br />
- 商人从政，比如泰国的他信，意大利的贝卢斯科尼和韩国的李明博与我们的预期并不相同。他们并不温和与务实，反而办事生硬，一如公司老板的作风，与军人倒有几分相像。此外，还会情不自禁地谋取私利。<br />
<br />
- 民主还需要坚持。目前，沙马总理的做法是正确的。如果军人和暴民要夺权，一定要让他们付出道义上的代价。<br />
<br />
- 民主必须在制度，而不是潜规则下面运行。军人和国王的特权，已经对民主乃至国家的运行有害了。<br />
<br />
- 解决日益扩大的贫富悬殊问题，是民主应该面临的挑战。泰国的民族同一性，泰国社会的宽容远胜于中国，贫富悬殊仍然造成这么大的问题，我们更要警惕。<br />
<br />
- 泰国的问题，对中国是有借鉴意义的。<br />
<br />
2008.9.6.</div>

							
							
							
							
