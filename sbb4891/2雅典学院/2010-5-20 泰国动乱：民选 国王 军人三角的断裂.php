																											<div id="postmessage_626947" class="t_msgfont">9.思考：民粹主义的阴影<br />
<br />
泰国的动荡，也与日益扩大的贫富悬殊有关。他信不够廉洁，也不够民主。但是，他满足了贫困农民的部分需要，因此在被推翻以后，泰爱泰党的化身仍然取得大选的胜利。<br />
<br />
以下是刚刚写好的《民粹主义或成世界趋势》：<br />
<br />
泰国很麻烦。被推翻的他信和现任总理沙马的政见很复杂，一方面他信有以权谋私的嫌疑，另一方面他信和沙马都主张帮助穷人，也实实在在做了一些事情。因此，他信被军人推翻以后，在很困难的条件下，前执政党的化身仍然在大选中取得压倒性的胜利。有人会认为他信和沙马代表穷人是假的，但泰国的穷人就是认可他们。<br />
<br />
反观泰国的中产阶级和民主派，他们在行动中多有欠缺之处。他信不民主，民主派总是希望国王和军人干政更不民主。当然，在中国，知识界也许有人认为，反对民粹主义就是正确的。<br />
<br />
泰国的局势，再加上一些拉丁美洲国家的局势告诉我们：民主正处在十字路口。在以往的政治运作中，中产阶级尚能够起到主导的作用，下层民众尚能够忍受不怎么惬意生活状况。我想，如果能够长期这样下去尽管不够公平，但也许还算是一种渐进的社会模式。不过，如果大众不想忍受了，中产阶级仍然拒绝改变，那就是僵化，中产者的温和与务实也就不存在了。<br />
<br />
在20世纪末和21世纪初，财富增长的趋势是迅猛的。同时，财富在社会中的分配也是有问题的。现在，成功者正在得意忘形，以为自己的成功就是对社会的贡献，甚至还是对穷人的贡献。有些人一毛不拔，却总是觉得穷人欠了他们什么。<br />
<br />
我在《论第二种自由――摆脱商业霸权的自由》中曾经谈到在民主制度下面，资本利用媒体的力量让大众看不到自己的需要。当时，我痛切地希望未来有所改变。看来，那时有些保守了。媒体是一把双刃剑，既能够让穷人处于幻想之中，也会使他们在幻想破灭之后产生绝望。既然教科书里面有天赋人权，媒体又天天展现荣华富贵，就必然会调动起穷人的诉求。<br />
<br />
有一个隐约的忧虑：在世界范围内，特别是在第三世界国家，民主正面临着类似古代罗马共和国末期那样的挑战。那时，元老派坚持民主共和，却无视平民的权益；凯撒考虑了大众，却最终走向了独裁。更加深刻的危机在于，随着资源的短缺，我们维持目前的贫富悬殊水平都很困难了。<br />
<br />
我们面临着两难的选择：按照常识，庇隆和查韦斯那样的民粹主义不会有好的结局。同时，现在的中产阶级沉溺于金钱的游戏，已经看不到穷人的需要。继续按照他们的意愿走下去，也许是一条死路。<br />
<br />
在以往的论述中，我多少有些民粹倾向。但是，我坚决地反对民粹主义。我认为，反对民粹主义的最好方法就是理解民众的诉求，拿出有针对性的对策。<br />
<br />
对于吴敬琏最近关于坚持改革的言论，我认为有正确的一面，也有坚持理念，不面对现实，拒绝改进的一面。说得尖刻一些，就是不考虑别人的死活。为此，想写《吴敬琏的坚持改革，有一半是空话》。<br />
