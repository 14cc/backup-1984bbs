													<h2>汪丁丁：我对“郎咸平现象”的看法</h2>
						
						
																											<div id="postmessage_436430" class="t_msgfont">原文地址：<a href="http://wang-dingding.blog.sohu.com/141878512.html" target="_blank">http://wang-dingding.blog.sohu.com/141878512.html</a><br />
<br />
汪丁丁：我对“郎咸平现象”的看法<br />
<br />
汪丁丁，北京大学国家发展研究院教授<br />
<br />
 <br />
<br />
最近几年，郎咸平是一种现象，转型期中国社会的现象。我的态度，在各种场合表述过，也被转述过，也引发过不少争议。干脆，一位网友的提醒，我今天就写一篇正式文章，表达我的看法。<br />
<br />
 <br />
<br />
我关注“郎咸平现象”，是因为它典型地反映了中国社会经济深层问题的积累已足以形成“干柴”效应，也称为“多米诺效应”。我曾撰文介绍格莱诺维特的理论，说明这一效应与社会骚乱的关系。更早，我还曾撰文分析了因医疗服务的长期弊端而积累至危险程度的普遍的焦虑。后者，是我们社会的心理疾病。<br />
<br />
 <br />
<br />
郎咸平对中国社会经济的批评，原本与我们的复杂自由主义立场的批评类同。我们不能赞同的，是郎咸平提出的改革方案。他的一系列“救世良方”之更严重的弊端，完全植根于他对政府的盲目信任，更遗憾的是，他从不意识到这一盲目性。<br />
<br />
 <br />
<br />
中国的事情往往是复杂的，例如，许多不同派别的学者可以类同地针砭时弊，如同民国初年的情形一样。关键在于，他们为中国社会开具的药方，决定了他们所属的派别。类似的情形，在今天，典型地，是人人可呼吁“改革”，人人可以“改革”为旗帜。关键是，怎样改革？于是显出流派之争。<br />
<br />
 <br />
<br />
简单地说，骨子里相信“市场”还是骨子里相信“政府”，这是一块试金石。郎咸平政策建议的基本思路，我们坚决反对。坚决反对，是因为，凡是在中国大陆长期生活并深感计划体制之弊端的中国人，大致都对中央集权有所警惕。而郎咸平政策建议的基本思路，客观上恰好是强化或倾向于强化集权的。我读他的文章得到的基本感觉是，他骨子里相信：“乱世用重典”。粗略而言，这一思路有道理。细细品味，难道二十世纪中国的百年乱世用的典不够重？关键在于，什么样的典？谁有生杀大权“用重典”？这才是“中国革命的根本问题”。国共双方，早期都参与了“中国社会基本性质问题”的大辩论，为什么？这算是我给“新政治经济学”读者的家庭作业。在这方面（即“治乱世用重典”），我始终认为，没有哪一个人――包括郎咸平想象中的他自己或任何其他人――能超过毛泽东。毛泽东之所以在全党全军具有“卡里斯玛”权威，主要因为他长期的卓越工作最终成功了中国共产党夺取政权的革命。我们可以容忍毛泽东晚年的许多错误，是因为我们还无法摆脱他的卡里斯玛魅力。<br />
<br />
 <br />
<br />
当代中国，毛泽东“卡里斯玛”权威之后的中国，其弊端之大源头，在道德沦丧与官僚政治，而不在“乱世”本身。前者是本，后者是标。郎咸平政策建议的基本思路不仅是治标而不治本的，而且很可能助纣为虐。<br />
<br />
 <br />
<br />
其次，回到主题，我关注“郎咸平现象”，因为正是这一现象，表明了中国大众心理深层躁动着的激进情绪。假如，让我们想象我们在一年内实行了真正民选基础上的多党政治，那么，这样普遍的激进情绪很可能成为政见最过激的政党的选票。熟悉民初历史的读者，1912年至1918年之间，我们缺少这样的过激政党吗？我立刻想到的是，无政府主义各流派，和保皇党的各分支。这样两极端的激烈斗争，很大程度上解释了二十世纪上半叶中国社会政治生活的残酷性。难道二十一世纪，我们还需要再一次经历这样的激进主义吗？不错，我们的社会记忆，实在太短暂。也因此，我们在复杂自由主义立场上，才必须不断唤醒社会记忆！<br />
<br />
 <br />
<br />
网络民主，不错，我也喜欢网络。但网络民主在中国这样的专断政治格局内很容易沦为民主暴政。因为，反正我们没有其它表达渠道。所以，我们不打算批判网络社会的过激党们，对他们的行为和表达方式（往往是谩骂），我们充分地理解。但难道这样宣泄之后，世界能好些？空想！自我麻醉！看看那些真正经历过社会变革的复杂过程的朋友们的思路吧。例如，罗小朋，他几年来设想的，是建构一个公共知识分子网络平台，通过认真和自由的对话，寻求公共政策的合理基础。小朋的年龄，或许比多数网络激进分子的父母还要大。与这样的老朋友相比，你们不觉得你们的“年轻”被你们浪费了吗？<br />
<br />
 <br />
<br />
郎咸平现象，它反映的是我们大众的普遍的缺乏思考。后者，普遍地表现为阿伦特感到切肤之痛的“平庸之恶”。<br />
