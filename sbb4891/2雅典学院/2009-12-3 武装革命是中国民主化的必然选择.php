													<h2>武装革命是中国民主化的必然选择</h2>
						
						
																											<div id="postmessage_353833" class="t_msgfont">作者：高光俊<br />
<br />
<br />
第一节．和平改良：失败的道路<br />
&nbsp; &nbsp;一、中国历史的教训<br />
&nbsp; &nbsp;在中国数千年历史中，无数改良变革都宣告失败，大到改变国家政治结构，小到施政改革，而国家政权演变都是经武装革命而形成，没有一次是通过和平改革而实现的。如果我们的哲学思想坚持凡存在的必然有一定合理性，那么中国改良变革失败必然有其内在的必然因素，尤其是长达几千年的历史经验，是任何人都不应忽略的，否则人类历史经验就变得毫无价值。究竟这种必然因素是什么呢？<br />
&nbsp; &nbsp;中华民族文化的实用性。中国文化的实用主义表现在宗教上便是不相信主宰万物的单一神的存在；从来没有政教合一的历史，更无欧洲中世纪教权凌驾于皇权的现象出现；从来没有十字军、伊斯兰教那样极端的宗教狂热。表现在政治上则是维护皇室及官僚阶层的特权利益，即使为此要使国家和人民蒙受巨大损失也在所不惜。所以在一个朝代建立以后，皇室及其官僚集团便竭力维护其利益，直至其武力不济而被推翻，新的利益集团又重复这样一种政治结构。<br />
&nbsp; &nbsp;二、世界历史的经验<br />
&nbsp; &nbsp;不仅中国几千年历史的演变是革命战争为主旋律，世界各国大规模的利益再分配也是通过革命实现的。西方和平改良成功的例子，只是发生在最近几百年，而且只有一部分成功。我们看到的东欧苏联共产党倒台也不纯粹是和平的，罗马尼亚实际上是一场军事革命，俄国直到1994年还有坦克炮轰国会的血腥争斗。<br />
&nbsp; &nbsp;三、中共的利益集团<br />
&nbsp; &nbsp;共产党的基本特点是政治上的一党独裁和经济上的公有制。中国共产党的几十年实践迫使他们不得不放弃公有制这一荒谬的理论，实行现在中国的半公有制、半私有制的经济结构，其发展趋势是公有制的彻底消亡。一党独裁带来中共的普遍、深入的腐败。越来越多的人认识到多党执政是社会发展的必然趋势，因而共产主义的理论证明彻底失败。然而另一方面，中共官僚集团正是开放改革的既得利益集团，他们清楚地认识政治改革必然导致共产党垮台，他们的既得利益将消失殆尽。这种思想支持他们拼命维护现存政权，目的不是信仰那个乌托邦的共产主义，而是自己的利益。六四期间，一位中共高官对他的子女和其他官宦子弟说：“你们不要跟着瞎闹。学运成功，倒霉的首先是你们这些人。什么主义不主义，那是假的！”这位1927年加入中共的知识分子高干正是中共官僚维护其政权的典型代表。<br />
&nbsp; &nbsp;四、中国民运二十年的教训<br />
&nbsp; &nbsp;中国现代民主运动始于1978年民主墙运动，经过北美中国民联，到1989年六四运动时发展到高潮。二十几年来，中国民主运动都以和平改良为其宗旨，但无论何种温和改良的尝试都遭到中共的残酷镇压。中共不仅对和平的民主运动采取残酷镇压，甚至对非政治团体（如法轮功）也大规模地镇压。<br />
&nbsp; &nbsp;这种镇压越持久，越残暴，和平变革的可能性便越小，因为变革后必将受处罚的中共官员越来越多。中共神经质的残酷镇压迫使越来越多的人走向武装革命的道路。中共似乎忘记中国历史上多少貌似强大的暴政都被人民武装革命所推翻。中共似乎认识到和平改变的时机已经或正在消失。所以，中国民主运动不能再只是单一寻找和平的方式争取中国民主，必须着手武装革命。正如一位资深民主人士感叹的那样，几十年来，他要么被中共象猫一样关进监狱，要么被中共作为交易品或礼物放出，深感自己无能为力。是的，这个力量从何而来，任何一位政治家都应严肃思考这一问题。<br />
&nbsp; &nbsp;中国民运必须注意革命问题的另一个原因是中国爆发大规模内乱的可能性。由于中共极端腐败，各种社会矛盾日益堆积而无法解决，且越来越严重，最终会有一天会爆发。这将迫使民运人士面对武装革命问题，否则将在中国政治舞台上毫无立足之地。<br />
&nbsp; &nbsp;五、台湾模式<br />
&nbsp; &nbsp;对中国和平改良还抱一丝幻想的人总是认为中国大陆可以象台湾那样走向民主政治。台湾从一党执政走向今天的多党执政，这是中国历史上破天荒的伟绩。但中共却不能走向这一步，这是因为中共的理论上就是一党专政，而国民党有从训政到宪政过渡的思想基础，因而转变过程中名正言顺，而中共如放弃一党专政，就不叫“共产党”。其次，尽管国民党下台后失去一些利益，但中共的腐败的深度和广度是国民党无法比拟的，他们必将为其利益而拼命维护一党独裁；再次，台湾的司法制度、新闻媒体、政治体系运转良好，可以顺利实现多党制，而中共则无法做到这些。所以，台湾模式是不可能在大陆出现的。<br />
<br />
第二节．武装革命：能够建立民主政权<br />
&nbsp; &nbsp;反对中国民主武装革命的一个理由是武装革命必然带来新的独裁专政。这个结论既不符合逻辑，也不符合世界历史的事实。从逻辑上看，建立民主政权是目的，武装革命是手段；革命后的政权形式是结果。手段可以多种多样，目的只有一个，结果可能不同。能否建立民主政权与通过什么手段去实现这一目的没有必然关系。武装革命既可带来一个新的独裁政权，也可实现一个民主政治；相反，和平改良同样也能带来民主政权或者独裁专政。<br />
&nbsp; &nbsp;从历史看，我们可以发现许多经武装革命带来民主政权的事实，如美国的独立战争、南北战争；而通过和平方式建立独裁政权者也存在，如墨索里尼、希特勒都是通过选举夺取政权并建立世界有名的法西斯独裁政权的。<br />
&nbsp; &nbsp;因而，能否建立民主政权取决于中国民主革命政党的理论基础和政治家的素质，而不是实现民主过程中的手段。<br />
<br />
第三节．革命的代价<br />
&nbsp; &nbsp;不赞成中国民主武装革命的另一思想是革命的代价太大。这种思想是浪漫而善良的，不幸的是不正确的、幼稚的。<br />
&nbsp; &nbsp;1、 革命是被迫的。<br />
&nbsp; &nbsp;中国有句名言：“逼上梁山”。所有的革命都是当局拒绝和平变革的产物，有了戊戌变法的失败，才会有后来的辛亥革命；有了六四大屠杀，才会有将来的民主武装革命。如果历史的发展必须要付出代价，我们别无选择。另一方面，即使我们不发动中国民主武装革命，谁能保证中国内部发生民众自发的武装冲突呢？因此，空叹革命代价太大只是消极的悲鸣而已。<br />
&nbsp; &nbsp;2、 和平改变的代价也不小。<br />
&nbsp; &nbsp;在中共统治中国半个多世纪以来，反抗中共基本上都是以和平方式进行，但我们的代价是6000万人的死亡和数亿人的被监禁。从人的生命和自由来看，“六四”后十几年我们付出的代价还算小吗？“六四”有几千人被屠杀，数万人被监禁；法轮功有一千多信徒被处死，一万多人被监禁，被拷打者不计其数；其他各教徒、政治犯每年有数万人被判刑关押。许多人被中共官僚枉捕、错判，中共每年处死数千人，是世界其他国家的总和。这样的代价还不算大吗？<br />
&nbsp; &nbsp;从经济角度来看，中共大小腐败官员每年侵吞人民数百亿元财产；每年吃、喝、嫖、赌、游玩不知花掉多少人民的血汗钱。中共贪官非法转移资金至国外日益严重，1997年是364亿美元，2000年则达480亿美元。自1990年以来，每年因官僚腐败造成的直接经济损失约在9800亿元到12， 000亿元，相当于全年生产毛额的13。2% 至16。8%，至于间接的经济损失更是无法统计。更恐怖的是，国家生态严重破坏，生态资源浪费惊人，中共存在一天。这种祸害便加剧一天。这种经济上的浪费不知从何处计算。<br />
&nbsp; &nbsp;3、 革命的代价是什么？<br />
&nbsp; &nbsp;革命的代价与武装革命的方式、持续时间的长短、中共的反扑有一定联系。武昌起义造成全国连锁反应的革命方式，其代价较小；革命战争时间越短，代价越小；中共瓦解越快，革命代价越小。<br />
&nbsp; &nbsp;革命的代价包括人的生命、自由和国家经济的停滞。不可避免，一些革命志士将在革命中失去生命和自由，普通民众也会受牵连，但革命除去中共大小贪官污吏这些国家的蛀虫、肿瘤，这不但不是革命的代价，而且是革命的成果，是应高举双手拥护的。革命自然造成经济的暂时停滞，但这是社会体制巨大变革的必然结果，即使是和平的改良，也必然带来经济的暂时停滞。俄国的休克疗法便是一个典型的例子。苏联共产党在基本和平变革中垮台，但俄国仍经历长达十年的经济停滞，直到现在才慢慢恢复过来。另外，消除共产主义这个肿瘤后，中国经济将会得到迅速发展，正所谓“磨刀不误砍柴功”。<br />
<br />
第四节．改良：革命的前夜<br />
&nbsp; &nbsp;中共的经济改革和社会改良不但不能缓和社会矛盾，反而深化社会问题，加速革命的到来。满清末年的变革几乎是今天中共改革的翻版。<br />
&nbsp; &nbsp;自鸦片战争以后，清朝不得不发展洋务运动，开始进行一系列经济改革和社会改良运动。<br />
&nbsp; &nbsp;首先是兴办实业。清朝在鸦片战争中，体会到西方船坚炮利的厉害，在围剿太平天国战争中尝到了洋枪洋炮的优越。于是开始办枪炮局。以曾国藩、胡林冀、左宗棠、李鸿章、张之洞、盛怀宜等洋务运动者创办了中国前所未有的制造厂、铁厂、纺织厂、电报局、铁路局等等。尽管中国大部分乡镇仍处在封闭的小农经济中，但现代中国工业也初见端倪，蓬勃发展。<br />
&nbsp; &nbsp;创办现代军队。不仅陆军开始逐步抛弃冷兵器，换上新式枪炮，而且中国开始大力创办新式海军。到1895年，中日甲午战争前夕，中国已建成世界上第七大海军，海军军舰的质量和吨位都不低与日本，成为亚洲强大的海洋舰队。<br />
&nbsp; &nbsp;传播西方思想。中国大量介绍西方文化思想，尤其是严复，他将西方文明的经典几乎全部翻译成中文，让中国学者能开始了解新的西方文明。<br />
&nbsp; &nbsp;开办新式教育。尽管废除科举考试是后来的事情，但洋务运动的开展让国人开始接受西方新式教育。算术、天文、地理、测量、化学、矿物等科学也越来越受到学者的重视。聘请西洋教师，送学子留洋西方渐成风气。<br />
&nbsp; &nbsp;这些变革对于沿袭了几千年传统文化的中国来说是巨大而深刻的，同时也给中国带来貌似强大的虚幻感觉。然而，这种变革却有一个极大的风险：社会政治体制的僵化。甲午战争爆发，清朝耗资巨大，苦心经营之“强大”的北洋水师顷刻覆灭。痛定思痛之后，国人番然醒觉，才认识到西洋文明的核心包括科学和民主两个方面，仅仅经济和社会的改革是不够的，必须进行政治改良。<br />
&nbsp; &nbsp;今天中共二十多年的经济改革，虽然深度和广度上不能与满清末年相比，但其对社会生活的影响是巨大的。然而中共却拒绝任何政治改革，如同当年满清一样认为经济的发展便是强国富民，但政治制度的僵化腐败必然使中国的“强大”变成泡沫，同满清一样。一旦再有个“甲午战争”，这种外强中干的黔驴便会被击倒。台湾海峡的战争风云便可能成为第二个“甲午战争”。这便是中共只是虚张声势恫吓台湾而不敢真正开启战端的根本原因。政治腐败已使中共变成一只真正的“纸老虎”。国民不可再被中共政府“开放繁荣”的假象所迷惑，要认识到只有民主政治才能带来中国真正的强大，腐败政治必将彻底毁灭中共号称“世界第三强国”的幻觉。中国民主革命政党不能坐等第二个“甲午战争”，再次经历军阀混战。</div>

							
							
							
							
