																											<div id="postmessage_725368" class="t_msgfont">取消国保是中国长治久安的第一步<br />
―― 致温家宝总理的公开信<br />
<br />
温家宝总理：<br />
<br />
我以一名普通公民的身份给你写这封公开信。如果你会使用翻墙技术阅读海外中文网站的信息，你一定可以在网上看到这封信；如果你不上那些墙外的网站，我不知道你属下的国保以及为你搜集情报的智囊会不会将它打印出来放在你的案头。我希望你有机会读到，有机会思考，有机会回应。<br />
<br />
我在媒体上看到了这样的一则报道：二零一零年六月二十三日，温家宝总理主持召开国务院常务会议，研究部署进一步推行依法行政工作。温家宝强调，必须清醒地认识到，当前中国依法行政体制机制尚不健全。今后一个时期，必须加大《全面推进依法行政实施纲要》实施力度，全面推进依法行政，不断提高政府执行力和公信力。这次会议还指出，依法行政包括“要严格、公正、文明执法。依法查处各种违法行为，维护良好的经济社会秩序。加强行政执法程序建设，落实和完善行政执法责任制，坚决纠正不执法、乱执法现象”。<br />
<br />
温家宝先生，你知道吗，就在你主持“依法行政”的国务院会议的同一天，我家门口又有一群国保前来“服务”了。这一天并非“敏感日子”，又没有什么外国元首来访，为什么你的“服务型政府”又来为我提供特殊服务呢？北京市朝阳区的国保王某打来电话，约我到小区的会所里聊聊。我断然拒绝之，我的时间很宝贵，我通常只跟我所欣赏的朋友聊天，而没有义务与那些无事生非的国保聊天。我转而一想，也许是前一天一直致力于为右派维权的铁流先生打电话邀我参加一个饭局，并发电邮告知，有茅于轼、张博树、滕彪等师友参加。我因早有计划要完成一篇文章，便回信说不能赴约。一定是国保偷看了我的电邮，看到我虽然表示不出席次日的活动，他们仍然担心我前去赴约，所以赶紧前来阻拦我出门，监视我的动向。温家宝先生，我想邀请你到我家中做客，你可以亲身感受一下贵政府是如何为一个只是努力说了一点真话的公民“服务”的。温家宝先生，你愿不愿意“现场办公”， “坚决纠正”这种“乱执法现象”呢？<br />
<br />
也是在同一天，推特上的一位朋友告知：“六月二十一日，派出所查到了我和余老师你的通信记录，连我的网易邮箱都报出来的，两个公安直接跑到我公司，质问我买你的书没。还要我谈谈对刘晓波观点的认识。不过到目前还算安全，没被请去喝茶。”最近几年来，我的作品不能在中国大陆公开出版，只能在香港和台湾出版。推特上有许多朋友想看我的新书，我便请这些朋友给我发电邮联系购买，然后将手中的少许书籍邮寄给他们。没有想到，先后有五位买书的推友，遭到国保各种不同形式的骚扰，其中有两人在国保的威逼之下将买到的我的书上缴给国保。这种肆无忌惮地侵犯公民隐私和私有财产的、如同强盗剪径般的行为，不是“非法行政”和“乱执法”又是什么呢？温家宝先生，你愿意亲自制止国保的此类恶劣作为吗？或者你是否可以拿出一个好办法来，将“行政执法责任制”落到实处？我不知那些被国保非法侵占的书籍去向如何，如果你渴望阅读我的著述，可以像普通推友那样直接跟我联系，我愿意卖给你甚至送给你。请你这位自称爱读书的人，不要剥夺其他公民读书的权利，好吗？<br />
<br />
温家宝先生，你自己一定心知肚明，在你和胡锦涛先生执政以来，“维稳”成为这个时代的“关键词”。你们将“维稳”这个词语像紧箍咒一样挂在嘴边，正表明中国社会的不稳定因素已经如同洪水滔天。二零一零年五月二十七日的《社会科学报》披露，二零零九年维稳财政预算执行情况令人震惊：全国内保费用达到五千一百四十亿元，已接近军费的数额，中央公共安全支出增幅达百分之四十七点五。温家宝先生，你刚刚当上总理的时候，曾经承诺将教育经费从百分之二提升到百分之四，但如今你的两届任期将满，这个数字几乎原地不动。这是什么原因呢？中国政府不是没有钱，中国政府是今天全世界最有钱的政府，但中国政府的钱都被用到哪里去了呢？有人说，公布的和秘密的各种维稳经费已经占国家财政收入的两成以上。“天价维稳”成为“胡温新政”的最大特征，“国保治国”成为一项至高无上的国策。从奥运会到世博会，国保的非法行政活动越来越猖獗，他们在奥运会和世博会期间查禁商店里的菜刀和老鼠药，他们不分昼夜地蹲守在每一个异议人士的家门口。互联网上的“敏感”词汇越来越多，日历上的“敏感”日子越来越多。所以，国保的经费越来越多、编制越来越大。<br />
<br />
有了国保的保驾护航，你们和你们的御用文人说，平安了，但这样的平安真的可以持续下去吗？熟读中国历史和世界历史的温家宝先生，你不会不知道，靠暴力和谎言是无法让一个国家和民族获得长治久安的。秘密警察不是维稳的柱石，而是和谐社会的最大的破坏者。执法犯法、肆意玩弄法律的国保们，败坏了这个社会的法治、信任与道德，他们污染的不仅是国土，而且是人心。他们不是致力于缓解矛盾，而是故意激化矛盾。他们表面上忠心耿耿，其实将你们推到火堆上烧烤。比如，长期搜集我的材料的北京市公安局国保大队的一名姓朱的国保，长年累月恐吓我身边的朋友、制造关于我的各种谣言、发匿名信和恐吓信，究竟是谁授权他这样做的呢？他每天的工作就是在犯罪，就是在败坏政府的公信力。温家宝先生，最近几年来编制和经费大幅增长的国保部门，与那些力量微弱的政治反对派相比，才是你们真正的掘墓人。温家宝先生，如果你拥有政治家的智慧与勇气，拥有敏锐的时代感与深厚的历史感，拥有对自己的国家和民族以及人类的未来负责的精神，那么，取消国保、停止对人民的恐吓，是你需要立即迈出的第一步。<br />
<br />
温家宝先生，如果你不从今天起便取消国保，国保未来的命运以及你的党未来的命运将不堪设想。东德的斯塔西和共产党的下场，便是中国的国保和共产党的前车之鉴。昂纳克等东德领导人曾经坚信，在无法挽回民心的情况下，让一个失去公信力的政权维系下去的杀手锏，就是秘密警察，即斯塔西。如果说在美学和道德上均丑陋不堪的柏林墙是公然向自由世界示威，并阻止本国居民“逃出疯人院”的企图；那么，斯塔西的作用就是让所有民众都不敢乱说乱动，至少“假装”对目前的生活表示满意，正如其负责人所宣称的那样，“国家安全部被赋予使用任何必要手段或方式阻止或扼杀所有阻碍或延滞社会主义成功的企图”。在此意义上，斯塔西堪称一堵用人肉来砌成的“柏林墙”。英国历史学家弗雷德里克&#8226;泰勒在《柏林墙》一书中指出，在德意志民主共和国存在的四十年内，至少有六十万人为斯塔西工作过。一些专家更是宣称这个数字高达一两百万。即便只算编制内的人员，即拿薪水的人的数量，那么每三百二十个东德人中就有一个斯塔西。相比之下，在希特勒德国，盖世太保的人数只有两万，即三千五百名德国人中有一个盖世太保。<br />
<br />
但是，斯塔西有没有挽救东德政权的崩溃和它自己的覆灭呢？历史的这一页已经翻过去了，就不必我来多说了吧。但我还是想向温家宝先生讲述一个有趣的细节：在东德政权存在的最后的时日里，斯塔西的末代局长、八十一岁高龄的米尔克，出现在新组建的最后一届东德议会。这个昔日让人仰视的人物，第一次遭到议员们的质疑和鄙视。这个白发苍苍的老人看上去很沮丧。“但是我很爱你们！”米尔克宣称，他几乎要哭出来了，“我热爱整个人类”。弗雷德里克&#8226;泰勒用讽刺的笔调写道，如果说米尔克怪异的情感宣泄中存在某种真实的成分的话，那就是对人类危险的爱，正如一支名为“警察”的摇滚乐队在他们的歌曲中唱到的如着魔般的爱恋：“你每一次呼吸，每一次行动，我一直都在关注你。”温家宝先生，米尔克的眼泪跟你的眼泪相比，哪一种更加真实呢？我相信，米尔克备受羞辱的这一幕，将来有一天必定会在中国重演。人类最愚蠢的品行便在于，经常重复演出同一出戏剧，而剧中人茫然不知。当年昂纳克跌倒的原因，难道会成为你们今天“雄起”的秘诀吗？<br />
<br />
温家宝先生，你不会不知道这段历史吧：东德政权瓦解之后，从昂纳克、克伦茨等前东德最高领导人到若干在柏林墙前枪杀逃亡者的普通士兵，都被送上法庭审判。一九九二年，统一之后的联邦德国成立了真相委员会，包括人权活动家、牧师雷纳&#8226;埃佩尔曼在内的二十七名委员，对东德政权下属的所有机构举行听证会，查阅所有能够找到的档案，共同撰写权威性的历史文件，形成了一份长达七百五十页的“总报告”，执笔者之一指出：“真正的任务不是审判，而是弄清发生了什么。这样，每一个人就能够在这部机器中找到属于自己的那个齿口。” 温家宝先生，你希望自己在未来的一份类似的报告中充当何种角色呢？你多次强调说自己很看重历史，那么你的内心深处是否真的害怕来自历史的惩罚呢？<br />
<br />
谈到东德的社会转型以及暴力之后的真相与和解，就不得不提及高克牧师（Joachim Gauck）。最近，高克得到了德国社民党和绿党的总统提名，尽管最终未能出任总统，但这足以说明他在德国受到的广泛的尊重。高克不仅是一位牧师，还是联邦政府首位调查前东德国家安全部档案的专员。德国之声报道说，这位一九四零年出生于罗斯托克的新教牧师，自一九九零年十月起担任这一被称为“高克局”的局长长达十年。所谓的“高克局”，实际上就是一个保存六百万份斯塔西秘密档案的图书馆，所有的德国公民都有权查阅本人的档案。高克说，他还是九岁的孩童时，就已了解到，社会主义是一个非法治制度。他的父亲被前苏联秘密警察逮捕，并在不给出任何理由的情况下被判处遣送西伯利亚强制劳动。年轻的高克希望进入大学就读德语文学专业，但当局不允许他入学。后来他学习了神学，并成为一名牧师，一九七零年在罗斯托克建立了一个教会。很快，高克就成为秘密警察的关注对象。对他们来说，高克过于积极地参与人权和和平事务，传道内容具有批评性。他们搜集了大量与高克有关的黑材料，却做梦也想不到，高克本人日后居然成为这些材料的管理者。在合并后的联邦德国，高克以沉着而坚定的方式管理前东德秘密警察留下的档案。他告诫人们不要忘记东德的历史，不要以怀旧的情绪回顾过去的“阳光灿烂的日子”。卸任档案馆的工作之后，高克担任了名为“反对遗忘，支持民主”的民间组织的主席。<br />
温家宝先生，你知道吗，我的梦想之一就是，未来的中国也能建立一所“高克档案馆”，我愿意成为其中的一名馆员。我相信，在我的有生之年会有这么一天。在那里，关于我的资料会有一麻袋，如果我要撰写自传的话，那里将是我最丰富的资料库。而与你有关的资料更是汗牛充栋，因为这些搜集资料的人都声称是为你的政府服务的。我想，那不是你的荣耀，而是你的耻辱。所以，为了让你的子孙和未来的公众在这些档案中尽可能少地发现与你有关的黑暗的内容，你是否愿意从此时此刻起就制止国保的为非作歹呢？<br />
<br />
温家宝先生，你理应认识到，那些肆意炮制冤案、打压弱势群体、制造人权丑闻的国保才是今日中国最不稳定的因素。你纵容甚至重用的这些国保，其实是一群屠戮人心的刽子手，是一群以国家的名义犯罪和作恶的凶手。当年，晚清诗人、思想家龚自珍在盛世的喧嚣中，喊出了末世的哀叹。龚自珍在《乙丙之际箸议第九》一文中写道：“当彼其世也，而才士与才民出，则百不才督之、缚之，以至于戮之。戮之非刀、非锯、非水火，文亦戮之，名亦戮之，声音笑貌亦戮之。……其法亦不及于要领，徒戮其心，戮其能忧心、能愤心、能思虑心、能作为心、能有廉耻心、能无渣滓心。”这不也正是今天中国的写照吗？“避席但畏文字狱，著述都为稻粱谋”，一个剥夺民众的言论自由和思想自由的国家是没有前途的，讳疾忌医就是自寻死路，连一个多世纪以前的龚自珍都明白这一点，一个多世纪以后的温家宝难道不明白这一点吗？<br />
<br />
孙志刚事件之后，上书人大呼吁废除收容遣送制度的北大三博士之一的滕彪，两年前曾经遭到北京国保绑架，此后他写了一篇题为《我无法放弃――记一次“绑架”》的文章。温家宝先生，不知你是否读过这篇文章？我诚心推荐给你好好看一看。在那次以国家的名义实行的绑架中，一个国保的小头目对滕彪说：“除了你的妻子和直系亲属，最了解你的，就是我。”为了达成这种“了解”，这些国保耗费了多少民脂民膏呢？最有意思的是这个头目另外的一段话：“你现在多好，有车有房，你开的是福克斯，我开的是奇瑞，你房子一百四十多平米，我九十平米。你的工作多好，家庭和睦，多想想自己对家庭的责任。孩子那么小，将来孩子问爸爸去哪了，让妈妈怎么回答？”是的，我们这些“被服务”的异类，居然可以生活得比国保们更好！难怪国保要愤愤不平了。但是，如果不想被扔进监狱，维持目前的小康生活，就乖乖听话吧。这样的循循善诱能够说服我们吗？我对这些即便出卖了人性，却也没有能卖出个好价钱的国保们充满了鄙夷与悲悯。温家宝先生，当你看到这段文字的时候，是激起了你取消国保的良知，还是反倒提醒你去增加国保的经费、提高国保的待遇？<br />
温家宝先生，请你切记，盖世太保、克格勃、斯塔西……那个秘密警察组织最终不是臭名昭著？中国的脊梁不是那些蔑视法律的国保，而是像滕彪那样依法维权的斗士。一个好心的朋友对滕彪说：“你被绑架的时候，在楼下挣扎狂喊了三分钟，都没有一个人敢出来看看。派出所来调查，也没有人愿意作证。这样的国民――值得你为他们去争取自由和人权吗？”我也遇到过好心人类似的劝说。滕彪的回答是：“值得。造成他们恐惧和冷漠的制度，正是我和千千万万个像我一样的普通维权人士所试图改变的。就算仅仅为了我的女儿不再生活在恐惧之中，我也无法放弃我的梦想，我的写作，我的行动，我的爱。我不会放弃。哪怕有一天我失踪以后，再也无法回来。”我也愿意将这段话作为我的回答。温家宝先生，这段话比你所有关于爱国、公正与良心的言论加起来都更有力量。我相信，克服恐惧之后作出此回答的中国公民将越来越多，终究会超过你麾下的十万名国保。中国的长治久安，必须迈出取消国保、解除报禁和党禁等步骤。瞒和骗、拖和等，只能病入膏肓、自取灭亡。温家宝先生，荣辱正反、生死存亡，你和你的同僚们还有最后的选择机会。<br />
<br />
<br />
中国公民 余杰<br />
二零一零年六月二十三日至二十五日<br />
北京家中<br />
