<br />
（一）北欧人，除了脑残，没人认为他们国家是“社会主义”。即使说他们“民主社会主义”，他们也认为是骂人。<br />
<br />
（二）北欧的社会民主党人，除了脑残，都认为“社会民主义”是指理念，而非“制度”。理念上，主张合作与分享。国家模式就是高福利的“资本主义”。<br />
<br />
（三）北欧的社民党人的强势，源于工会的强力支撑。工会力量大，政客都怕怕。如果你在工会面前畅谈“低人权优势”，他们会冲上来撕烂你的嘴。<br />
<br />
另外，还有我在网上看的相关资料。我也不知道是否确切，抄下来，给民主控看看。开启民智，兼听则明嘛。<br />
<br />
1.北欧国家的私有制非常稳固，社民党执政四十年，瑞典商业之85%，制造业之94%归私人所有。因此，瑞典社民党前主席布兰亭说：瑞典是“妥协的国家”，社会民主党在既定的体制之内，代表理念，而不代表阶级。<br />
<br />
2.九十年代中后期之后，北欧国家采取过调整：政府干预减少，取消补贴、价格控制等；改造国企，某些变股份制，某些干脆关掉；降低税率，个人所得税边际税率和企业税都降了大半；福利下调，瑞典从90年代开始决定将病假工资由原工资的90%，改为头3天为65%，之后是80%，自第91天起恢复到90%等等。<br />
