													<h2>讨厌秦晖的奴役思想</h2>
						
						
																											<div id="postmessage_701199" class="t_msgfont">　　民主控们的品味常常令我很无语。某月某日在某论坛看到他们围观一篇帖子，振臂高呼，好！好！太好了！我好奇了，从人群中艰难地挤了进去，一瞧，原来不是公审大会，是带功报告大会呀。秦晖坐在讲台上手握麦克风，咳嗽几声，台下的亲友团挥舞着荧光棒，标语口号翻云覆雨的，“政右经左”、“中间道路”、“民主不民粹”等等。<br />
<br />
　　秦晖，不就社会民主主义，犯得着那么大动静吗？当年在欧洲，社会民主党人最鼎盛的时期，像个交际花左抱抱右亲亲，但没掐赢共产党，也没扳倒法西斯，自己却闹得家破人亡，鸡飞狗跳。社会民主党人都是滥好人，贪名、软弱、糊涂，理论一大套，生活里却不辨忠奸、认鸡作凤。讲个女人的例子吧。汉娜阿伦特，冰雪聪明，却无可救药地爱上海德格尔。有人说，这是盲目的爱情。我不这么看，阿伦特的父母都是社会民主党人，阿伦特的脑子在耳濡目染之下，本来就亲近海德格尔的哲学。后面的事实则证明，海德格尔的哲学侍奉纳粹主义。阿伦特到了美国后，执着地探索“极权主义”之谜，那是因为这劳什子支配了她的一生。她不仅爱上亲纳粹的海德格尔，还爱过一个马克思主义者。可是遗憾呀，她到死都没明白过来，真正的根源在她父母那里，他们信奉社会民主主义。<br />
<br />
　　中国的社会民主故事就更搞了。民主控们反老蒋反独裁，最后给山寨大王毛主席站台去了。在他们的鼓噪下，当时左倾的美国政府更疏远老蒋了，于是共产党顺利打下江山。中国人民站起来后，民主控们欢喜鼓舞地“协商参政”，不知天高地厚，渐渐以功臣自居，以谏示忠。这下老毛又不高兴了，知识分子唧唧歪歪，于是连弄几场运动，把全国人民都连累进去了。老毛死了，民主控们喊冤，要求平反。再后来，有人写回忆录，往事并不如烟，和中共拍艳照好傻好天真。实际上他们手上也有血腥，不是因为残暴，而是因为愚蠢。相比之下，中国自由控机灵多了，领头羊胡适跟老蒋跑去台湾。他们固然也郁闷过，但《自由中国》毕竟办了二十多期才停的，蒋家王朝是仁慈的。更何况，小蒋临死还是让台湾民主了嘛。现在呀，大陆的民主控拼命想要的，台湾的自由控都玩腻了。<br />
<br />
　　我扯这么远，无非想提醒各位，社会民主主义是靠不住的。作为这一路线的代表人物，秦晖也是靠不住的。去年《08宪章》，他就没签名。本来这是自愿的事情，没人怪他。但他非要公开声明，自己因理念不合没有签名，这就奇怪了。因为秦晖向来主张“共同底线”，就低不就高，怎么关键时刻就变了脸，挑剔起来了？这还不算过分，更离谱的是他这边没签，那边却在一份给温总理的奏折上签了名，要求调高燃油税。和他一起签名的还有左王汪晖，当年自颁学术大奖，名震江湖。加税！皇上不急太监倒急了，这不是奴才思想是什么？！我知道很多人会说，秦晖一时糊涂嘛。其实不然，这才是真实的秦晖，虽然他很能讲漂亮话，但根子上看他的思想非常危险，有一种奴役民众的倾向。就不讲远了，以他最近的一篇谈话《经济危机产生的根源》为例吧，有这么一段话：<br />
<br />
　　“在民主制度下会造成老百姓既追求高福利也追求高自由的这样一种冲动，这些国家左派上台减低不了自由，但是可以增加福利，右派上台减低不了福利但是可以增加自由，西方的右派都是反对福利国家，但是西方右派上台从来没有导致过福利国家的弱化，里根时代是这样，英国的撒切尔时代也是这样，民主条件下他们弱化不了，老百姓不干，老百姓既然不愿意弱化福利国家，为什么把右派选上台呢？道理很简单，他们不愿意降低福利，但是他们愿意提高自由，选左派是不愿意减低自由，但是愿意提高福利。<br />
<br />
　　增加自由在什么地方可以增加呢？政治自由基本上没有增加的余地，实体的财产自由也没有什么余地，只有向虚拟方向发展。因此所谓金融业的过度自由其实在某种意义上是有必然性的。”<br />
<br />
　　一派胡言！什么时候“自由”竟成了政府的调控目标，就像水库，觉得满溢了就泄洪，觉得干涸了就放水。在西方的政治争论中，与福利相关的问题是税收。左派倾向于扩大政府功能，为增加税收，常用的借口是增加福利，另外还有一个借口就是捍卫自由，所谓“自由依赖于税”。左派强调，强大的政府才能保护公民的个人自由。右派呢，他们反对高税收，认为政府以福利之名干预分配，最终会破坏了个人自由。即是说，在西方，政客无论左右，都高举着自由的旗号。从来就不存在这样一条能量守恒定律：减低自由提高福利，或减低福利提高自由。一般而言，左派上台，有可能因为选民被高福利的承诺吸引；右派上台，则可能是因为选民被减税的承诺吸引。但不管怎样，这两种承诺都被包装成了同一种东西――自由。<br />
<br />
　　秦晖将美国的金融创新说成政府在“增加自由”，那就更扯了。金融衍生工具的发明，虚拟经济的急速膨胀，不是政府有意而为之，而是政府干预造成的流动性泛滥的结果。钱太多了，价格信号紊乱了，会给投资者一种错觉，明天的明天的明天会更好。有了这么多乐观的投资者，有了那么长远的眼光，自然就会有千奇百怪的金融产品满足他们的需求。这怎么能说是“增加自由”呢？搞传销的多了，卖墓地的多了，开赌场的多了，做典当的多了，这能增加谁的自由呢？自由是能被政府“增加”的东西吗？如果是，秦晖的意思莫非是，自由本是政府赐予的？<br />
<br />
　　虽然秦晖被视为自由派，但美国的自由派从来不敢这么明目张胆，将“自由”视为一种可以让政府牺牲掉而换取福利的东西，尽管这类人倡导的福利政策有可能实际上损害个人自由。秦晖这种聊斋般的自由观，如果说曾经在现实政治之中实践过，那就只有苏维埃政权和法西斯政权了。区别可能在于措辞，布尔什维克用“专政”来解释“自由的调控”。相比之下，纳粹心直口快，只有他们公然宣称，为了国家利益，每个人都应放弃一点自由。当然纳粹比秦晖要走的更远，他们还认为，犹太人应放弃全部自由。<br />
<br />
　　说到这里，当我把秦晖比做纳粹里的温和派，就已经感觉到民主控们的怒火啦，他们的板砖将像暴雨般朝我兜头倾泻。我预感到自己又犯了众怒，以后可能很难嫁得好。好吧，让我死于嫁错郎前多说两句。我想提示各位，秦晖独创了一个概念叫“低人权优势”，用来解释中国经济发展之谜。我不想引入今天中国的经济增长是因为“私有化”还是“低人权”的争论。我只想请各位从字面上去理解这个概念，好好想想，如果“低人权”可以成为“优势”，统治者是不是就该为了保持“优势”而保持“低人权”呢？胡温拒绝改革的理由就更充分了呢？――伟大的思想家秦晖不是说了嘛，低人权有优势。<br />
<br />
　　假如今天世界上都是奴隶制国家，秦晖见识短浅，胡说瞎诌，我也就算了。可是放眼看过去，众多的西方发达国家里，哪一个以“低人权”换来了优势地位？一个文明社会里的正常人，会把人肉包子当成某包子铺的优势吗？历史地看，能体现秦晖“低人权优势”的无非就是斯大林的劳改营和希特勒的集中营，据说就是这些才带来了他们的经济奇迹。但这不过是以讹传讹罢了，当大部分人都牺牲掉自己，去供养金字塔尖少数人的时候，经济奇迹没有发生，但是关于“经济奇迹”的谎言却不绝于耳，因为统治者需要人人获益的幻象来维持自己的“合法掠夺”。<br />
<br />
　　在目睹了上个世纪苏维埃政权和法西斯的罪恶，自己也经历了大跃进和文革的“低人权贫困”之后，秦晖作为一名有良知的知识分子，居然相信“低人权优势”，真是让人感慨万千啊。谁才是权势的帮凶呢？让我肯定地告诉各位，不是犬儒主义者，而是秦晖这样的社会民主主义者。他们宣扬的是奴役思想。今天的民主控们在台下反对主子，明天他们上台就成了新的主子。民主控们今天台下是造反有理的帮凶，明天上台就是奴役有理的凶手。</div>

							
							
							
							
