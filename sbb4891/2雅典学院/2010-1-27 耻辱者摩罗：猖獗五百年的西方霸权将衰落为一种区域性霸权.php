													<h2>耻辱者摩罗：猖獗五百年的西方霸权将衰落为一种区域性霸权</h2>
						
						
																											<div id="postmessage_453411" class="t_msgfont">未来三十年中国文化展望<br />
<br />
（猖獗五百年的西方霸权将衰落为一种区域性霸权）<br />
<br />
摩 罗<br />
<br />
随着经济的发展和政治的崛起，未来三十年，中国的文化发展也必将出现新格局。具体说来，我对未来三十年变化的预测，可以概括为对三重精神枷锁的摆脱，文化资源上的回归，和国家认同危机的减弱。<br />
<br />
摆脱三重精神枷锁<br />
<br />
中国国门被西方强盗的炮弹打开之后，中国人在精神上逐步形成了几重枷锁。三个摆脱是针对三重枷锁而言的<br />
<br />
第一重枷锁是国民劣根性的枷锁。外国殖民者和中国精英不断告诉国人，我们中国为什么打不过西方，因为我们人种有问题，我们具有精神的、道德的劣势，西方人具有种族的、精神的、道德的优势。总之，我们国民性不行，我们普遍具有国民劣根性。<br />
<br />
第二重枷锁是“西方文化崇拜”。鸦片战争失败以来，我们的精英群体认为西方文化具有优势，我们中华文化是腐朽的没落的，必须彻底根除，只有学习西方文化才有出路。于是五四一代送给我们一个秘方：全盘西化。五四以后，中国的道路大致上就是按照这个秘方走的，越走越出现强烈的“西方文化崇拜”。<br />
<br />
第三重枷锁是“西方国家崇拜”。后毛泽东时代，为了鼓励中国摆脱僵化的计划经济体制和思想，中国的政治精英和文化精英共同编造了西方国家的乌托邦神话，于是，国人对于西方社会由五四时期的“文化崇拜”发展为八十年代、九十年代的“国家崇拜”。这种“西方国家崇拜”观念在今天依然统治者许多精英人物的大脑。五四时代谁要是对西方文化发表一句不恭之论，必有人拍案而起。上世纪八十年代以来，谁要是对西方国家略有不恭言论，必有大批洋奴群起而攻之。<br />
<br />
未来的三十年，这三重枷锁将会逐级砸碎。<br />
<br />
首先被砸碎的将是“西方国家崇拜”的枷锁。随着中国与国际社会交往的日益深化，相互的了解和尊重不断增加，利益的纠缠和博弈也在不断增加。中国人将会亲眼看见西方国家不是一个道德的存在，而是一个利益实体。那种乌托邦式的崇拜将会不攻自破。<br />
<br />
其次被砸碎的将是“西方文化崇拜”的枷锁。前人相信西方世界对于东方世界的殖民征服是因为他们具有文化优势，这是一个极其错误的理解。英国学者汤因比在跟日本学者池田大作聊天时说过，迄今为止所有人的经济优势，无论是国家内部某个阶级的经济优势，还是国际社会某些国家的经济优势，都是通过掠夺和剥削他人而实现的。在全球化的过程之中，中国人正越来越明白这个道理，日后将不会像五四一代那样，将西方社会的富强理解为文化优势所致。<br />
<br />
最后将会砸碎“国民劣根性”的枷锁。这重枷锁是西方殖民者为了奴役东方殖民地人民而特意打造的，有的殖民地人民从来不曾认领之（比如中东社会和拉丁美洲社会），可是中国人从梁启超开始就有点相信我们确实具有某些国民劣根性，五四一代更是从殖民者手里接过这道枷锁，十分虔诚地戴在自己的脖子上，而且主动地越勒越紧。<br />
<br />
鲁迅一辈子最主要的文化工作，就是让国人相信，我们中国人在精神上、道德上、教养上确实不如西方种族，确实具有国民劣根性。我们的语文教师，结合鲁迅的作品，这样给青少年一代灌输国民劣根性学说和逆向种族主义思想，已经灌输了大半个世纪。<br />
<br />
至今为止，深受五四文化熏染的咱们这几代人，对这样的谬论依然深信不疑。但是，在中国崛起过程中成长起来的80后、90后、00后们，将不会认领这种枷锁，他们必将起而将这样荒唐的精神枷锁砸碎。<br />
<br />
文化资源的回归<br />
<br />
砸碎了上述三重精神枷锁，中国人将会明白，文化权利就是国家利益和民族利益的组成部分，“全盘西化”的思路何其荒唐之盛也。中国要想成为世界大国，必须仰仗自己的文化屹立于世界。<br />
<br />
所以，我预料往后的三十年，必是中国社会向古老的中国传统文化回归的三十年。五四一代所砸碎的，我们必将修复，五四一代所抛弃的，我们必将一件件找回。<br />
<br />
中国的人文文化和道德文明，以周代为起源的话，一直到康有为、梁启超时代，指导中国人的生活长达三千余年。戊戌变法时期，康梁在主张学习西方社会的器物和制度的同时，坚定地捍卫中国的精神文化。<br />
<br />
在戊戌变法失败之后的岁月中，虽然梁启超于1902年写过《新民论》，一定程度地接受了西方殖民者对于中国国民性的若干诬陷性描述，但是他很快就觉悟过来，不再从文化优势方面解读西方势力的扩张。尤其是他考察过一战刚刚结束的西方社会以后，更是对西方文化完全失望，转而一头扎进中国传统文化之中，企图从中国文化中寻找挽救世界的良方。<br />
<br />
康有为更是从不间断地从中国传统文化中寻找资源，用以拯救中国的覆亡。他甚至参照西方以基督教为国教的经验，积极主张将儒学宗教化，要求政府以儒教为国教，以此拯救中国、造福世界。他成立孔教会，主持祭孔仪式，号召读经，在在都是为了借助中国的文化认同，维系中国的凝聚力，以便更加有力地与一切正在掠夺或窥伺中国大地的虎狼国家博弈与决战。<br />
<br />
当代“文化民族主义”思潮的代表性学者康晓光赞颂康有为的一段话，可以用来赞颂康有为、梁启超师生二人。康晓光说：“一百多年后，重温他的那些文章，我们不但能够感到一种高瞻远瞩的伟大智慧，还能够体验一种不可战胜的自信、博大恢宏的气度、死而后已的责任感、对理想的无限忠诚、挑战社会潮流的无畏勇气和对历史使命的自觉承担。”<br />
<br />
五四一代逆康有为思想而行之，实际上是精神崩溃后慌不择路所致。如果说他们当时那种惊世骇俗的选择还有若干现实的理由，今天，我们如果还要沉溺在“全盘西化”以及“西方崇拜”之中，实在找不到一丝借口。文化上我们唯一的正道就是回归康梁，回归中国文化的正统。这将是往后三十年间文化运动的主旋律。<br />
<br />
至于“融合中西”、“贯通古今”之类的格言，当然也不可不提及。因为简单的复古之路，世界上从来不曾出现过。<br />
<br />
国家认同危机的缓解<br />
<br />
在解决文化回归的同时，往后三十年将会化解目前的政治认同危机，出现较好的政治认同和国家认同。<br />
<br />
当今中国，政治观念和思想之纷乱，恐怕不亚于战国时代和五四时代。这种纷乱不但造成了政治认同的危机，甚至造成了民族认同和国家认同的危机。<br />
<br />
五四一代的思想遗产，给几代中国人造成了巨大的离心力，许多学富五车的精英人物，他们的知识结构、他们的人生哲学、他们的思维方式、他们讨论问题的立场、他们观察问题的视角，都是西方的，他们的言论往往也是代表西方利益的。他们以生活在中国作为最大的痛苦，他们以作一个中国人为最大的耻辱。移居西方国家是他们最大的理想，得到西方国家具有政治意识形态背景的奖金是他们最大的骄傲，他们对于中国的诅咒与西方国家的诅咒不谋而合是他们最大的欣慰，将国家和公民的财富掠夺转移到西方国家的银行是他们最得意的胜利。<br />
<br />
中国的民族认同和国家认同因此隐藏着深刻的危机。<br />
<br />
三十年之后，当中国崛起为世界大国甚至主导性国家之后，中国的政治凝聚力将会大大提高，中国的民族认同危机将会大大缓解。<br />
<br />
当这一切问题得到解决的时候，正是今天的80后、90后、00后成为中国社会的主体人群的时候。那时候，正好是鸦片战争发生两百年纪念日。两百年的苦难和屈辱毁灭了几代人的幸福，但是没有毁灭这个伟大的民族和国家。这个国家，这个民族，经过两百年的生死煎熬和拼搏，将会把猖獗五百年的西方霸权约束为一种区域性霸权。当80后、90后、00后也像我们这一代一样退居社会边缘的时候，他们的后代在充分吸收西方文明之精华之后，将会使中华文明成为地球上的主导性文明。</div>

							
							
							
							
