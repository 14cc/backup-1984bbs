													<h2>摩罗：奥巴马为中国的崛起剪彩送行</h2>
						
						
																											<div id="postmessage_453412" class="t_msgfont">奥巴马为中国的崛起剪彩送行<br />
<br />
 摩&nbsp;&nbsp;罗<br />
<br />
美国总统奥巴马前天从上海飞到北京。昨天上午跟中国国家主席胡锦涛举行会谈，然后共同举行记者见面会。前天晚上胡锦涛以个人名义宴请奥巴马，昨天晚上胡锦涛举行盛大国宴，招待奥巴马一行。<br />
<br />
昨天上午，我守在电视机前，看了有关奥巴马访问中国的全部报道。今天上午，又从电视里看了昨天晚上胡锦涛与奥巴马在国宴上一起欣赏文艺演出的情形。<br />
<br />
也许，真正意味着中国崛起为大国的标志性事件，是美国总统奥巴马的来访。由世界霸主的总统来到中国，以平等的姿态跟中国领导人共商双边关系的一系列大事，以及关系到世界格局和福祉的全球性问题，这说明中国在世界舞台上的大国地位已经得到确认。<br />
<br />
事实上，在拯救世界性的金融危机过程中，国际舞台上大多数具有发言权的国家，都把中国当作举足轻重的大国强国看待。当大家都认为你是大国的时候，你即使实际上还不是大国，也已经享有着促使你成为大国的一种战略资源――别人的认可就是你的软实力。<br />
<br />
奥巴马抵达中国前夕，在日本发表演讲指出，美国不谋求遏制中国。这个表态标志着中国最近几年不会遭遇来自美国的战争威胁。以往几百年，或者说几千年，一个强国取代前一个强国的老大地位时，几乎全都是通过战争完成的。如果这几年中美之间没有战争，往后美国就更加不会挑起中美战争，因为中国的块头一年比一年伟岸高大，一年比一年不好对付――中国可不是当年的越南巴拿马，也不是今天的伊拉克阿富汗，未来的中国就更加不是。<br />
<br />
奥巴马这次访华，其战略要点在哪里？他公开承诺不遏制中国的发展，承认中国正在崛起为世界大国的事实，颇有点为中国的崛起剪彩送行之意味。<br />
<br />
由世界霸主主动给中国的崛起剪彩，光荣啊。<br />
<br />
当然，他的剪彩是为了跟中国做一笔交易。他希望中国承诺在崛起过程中不损害美国的利益，也就是继续尊重以美国利益为中心而建构起来的当前世界格局及相关游戏规则，并帮助美国维持这个游戏规则的有效性。此外，还必须继续为他们当下的经济危机买单。<br />
<br />
看来中国只能满足美国的要求。<br />
<br />
实际上，就中国的经济实力、军事实力、科技水平、战略水平而言，中国在十年之内也必须忠实履行这种承诺。十年之后，中国才可能有能力真正跟美国平起平坐。那时候的世界格局，将是一个双黄蛋。这种双黄蛋的局面将会维持若干年。<br />
<br />
我是直到两年之前，才偶尔从一本杂志上读到，中国将会在2050年取代美国成为世界第一强国。我当时感到无比惊讶，因为此前我只认为美国是昆仑神仙高入云端，中国是牛圈耕夫命途多舛，二者不可能并驾齐驱，更不可能想到“彼可取而代也”。<br />
<br />
仅仅两年之后，中国已经颇有点脱颖而出的意味。发生于美国的这场金融危机，让中国提前十年享受到大国的尊荣。美国因这场金融危机丢分甚多，在挽救危机的过程中，还继续丢分。中国在克服危机上却连连得分，引来国际社会刮目相看。难怪有人说，中国是这场经济危机的最大受益者。<br />
<br />
准确的表述应该是这样的，中国在这场经济危机中，无辜地受到牵连，战术上遭受极大损失，但是战略上却有极大的意外收获。中国提前十年被国际社会当作世界性大国而受到尊重，中国发展模式也因此受到世界关注，这就是中国的战略收获。<br />
<br />
如果没有这场经济危机，奥巴马的这些言词，将会等到十年之后由下两届美国总统的声带发出。<br />
<br />
以前老听人高喊振兴中华，似乎越听越不敢指望。没想到突然之间这中华还真颇有一点振兴的意味。只要美国一些智囊公司无数进攻中国的军事计划十年之内不被政府付诸实施，我看中国的强国交椅就基本上坐稳了。到那时候，中国就可能敢于甩出大手笔，从容不迫地把内部的事情收拾好来――从体制到环境，从三农问题到公民权利，从民主机制到社会保障体系等等。<br />
<br />
六十年前，一位日后命途多舛的诗人高喊道：时间开始了！时间开始了！那份激情喷涌的热望感人至深。<br />
<br />
自满族入关以来，自英国军舰进港以来，自俄国强盗掠夺雅克萨、海参葳以来，自日本践踏我台湾和东北甚至几乎遍及全中国以来，自西方列强火烧圆明园、屠戮北京城以来，中国人一直在谋求恢复文明古国的尊严和世界强国的威仪。这场艰苦卓绝的奋斗持续了将近四百年，即使从鸦片战争算起，也已经快有两百年了。<br />
<br />
几个世纪的奋斗和牺牲，中国确实在起死回生，好像真的是“时间开始了”。<br />
<br />
开始了就好，千万不要因为内外的什么折腾而再次跌入低谷。<br />
<br />
一个低谷的轮回，得要几百年啊！</div>

							
							
							
							
