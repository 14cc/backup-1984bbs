穆斯林，现代社会的高科技野蛮人？<br />
<br />
作者：同人于郊<br />
<br />
　　说明：这是一本书，《America Alone》的读书笔记。作者Mark Steyn是美国保守派，支持共和党，支持伊拉克战争。我发此读书笔记并不表示我赞同其观点。完全是开卷有益，美国右派的思想，对大多数中国人来说是陌生的。但我的确认为作者所讲述的问题是真实的而且是严重的。我理解这本书最主要的思想并不是人口，或者伊斯兰，而是西方文明的衰落。对于欧洲福利制度，社会民主主义，这一套东西，读了此书我们会有一个 second opinion。对于美国为什么允许一般人持枪之类的问题，我们也会加深一点认识。全书涉及中国部分很少，版主们不必担心。<br />
　　<br />
　　我写读书笔记的原因：<br />
　　1。就好比围棋打谱，是一种学习。总结作者的思想，看清其逻辑脉络。眼过千遍，不如手过一遍。<br />
　　2。以后查找什么事实，观点方便。<br />
　　3。发在网上可以跟网友们一起讨论研究，比自己看书有意思。<br />
　　<br />
　　America Alone: The end of the world as we know it<br />
　　<br />
　　副标题的意思是说&quot;我们知道的这个世界&quot;的结束，西方世界的结束。<br />
　　<br />
　　Prologue<br />
　　<br />
　　现代人总觉得应该担心一些事情，he feels good about feeling bad。有什么事最值得担心呢？<br />
　　- 核大战么？那是上世纪80年代以前大家担心的事情，那时候大家对核大战如此担心花样百出，结果证明核大战的担心根本是多余的。<br />
　　- 全球变暖？是，Gore 整了个 An Inconvenient Truth，可是海平面上升不知道要等多少年才能发生。<br />
　　<br />
　　但眼前就有一个重大危机：西方文明快要消失了。<br />
　　<br />
　　其实说到全球变暖这样的&quot;预言&quot;，历史上不知道发生过多少次类似的：<br />
　　- 1968年资深科学家 Paul Ehrlich 出畅销书预言1970年地球上数亿人将死于饥荒；<br />
　　- 1972年Club of Rome的研究表明世界将于1981到1993年把金属等资源消耗光；<br />
　　- 1976年Ponte预言新的冰川时代；<br />
　　- 1977年卡特总统预言十年之内石油消耗完毕。<br />
　　以上这些都没发生。这些人被称为 doom-mongers（世界末日贩子）。<br />
　　<br />
　　然而从1970到2000年有一件事情是真实发生了的：发达国家人口从占世界总数的30%缩小到刚过20%，穆斯林世界总人口则从世界总数的15%增长到20%。<br />
　　<br />
　　1970年穆斯林以外没有多少人拿伊斯兰当个大问题，然而今天它是全球性的。9/11并不是 &quot;the day everything changed&quot;，而只不过是揭示了早就改变了的世界形势。这个形势的改变是由三个因素组成的：<br />
　　1。人口减少；<br />
　　2。发达西方社会民主国家的不可持续性；<br />
　　3。文明社会的衰竭。<br />
　　<br />
　　People Power<br />
　　<br />
　　有人力才有一切，而且还必须要有年轻人，你才能干大事情，比如闹革命什么的:) 关注&quot;中东和平进程&quot;的人是否注意到一个事实： 加沙地带人口年龄中位数是15.8岁。一群失业，没受过教育的小孩，你怎么让他们有理性？不考虑这个因素怎么可能解决巴勒斯坦问题？<br />
　　<br />
　　与此同时欧洲的情况则是缺孩子。维持一个社会人口，需要的生育率（平均每个妇女生几个孩子）是2.1。现在欧洲，希腊是1.3，意大利1.2，西班牙1.1！相比之下美国正好2.1，新西兰略低一点。<br />
　　<br />
　　现在为什么到处都是穆斯林在惹事？因为人有人力。1970和1980年代，穆斯林世界在努力生孩子，而西方世界仍然在愚蠢的说什么人口过剩资源不够危机。Diamondd的书《Collapse》，好像什么社会崩溃都跟砍树有关。其实错了，不是tree，是 fammily tree，是孩子。俄罗斯人口逐年减少国家都快完了，跟树有关系么？<br />
　　<br />
　　Welfare and Warfare<br />
　　<br />
　　欧洲的人口减少，与欧洲国家这种社会民主主义的福利制度能否持续下去密切相关。在美国政客们辩论的是福利把太多债留给子孙，欧洲的情况则是根本没有子孙来替你还债。<br />
　　老龄化 + 福利 = 你的灾难<br />
　　年轻 + 意愿（will） = 任何敢挡你的人的灾难<br />
　　<br />
　　意愿，是一种文化概念。比如说非洲人就没有意愿，因为他们并不认为自己是&quot;非洲&quot;人，他们认为自己是自己部族的人。伊斯兰是有意愿和年轻，欧洲则是老龄化和福利。欧洲人口减少没有人来维持福利，只能引进移民，而移民来的大多是穆斯林：冲突不可避免。<br />
　　<br />
　　Fighting Vainly the Old Ennui<br />
　　<br />
　　欧洲的真正问题在于大政府。从摇篮到坟墓，什么都有政府管，人民根本不想依靠自己，没有自信。冷战本来是欧洲和美国的共同胜利，但很少有欧洲人为此感到高兴，因为欧洲人本来也没在其中做出什么贡献。相反苏联的解体只是加快了整个欧洲的衰弱。<br />
　　<br />
　　伊斯兰恐怖分子跟历史上的&quot;印第安恐怖分子&quot;截然不同，他们直接进入你文明城市的腹地，他们有现代化技术，甚至核武器。基地组织是跨国的，全球化的，连锁的，甚至外包的。面对这样的敌人，欧洲人有军事上的优势，但是没有这个意志去与之作战。<br />
　　<br />
　　作者引用了本拉登的一句话：When people see a strong horse and a weak horse, by nature they will like the strong horse。西方军队看上去像强壮马，但是如果你不敢用这个军事力量，人家就当你是弱马。这不仅仅是欧洲，美国也有这个问题。<br />
　　<br />
　　现在一个有意思的现象是全世界都讨厌美国。本来是正常的，因为所有强权都招人恨，但是大家恨美国的原因很奇怪：比如穆斯林恨美国的脱衣舞，欧洲人恨美国反同性恋和堕胎，等等，世界人民恨美国的可乐，汉堡，美国什么都不对。造成这种现象的原因可能是美国是世界上第一个非帝国的超级大国。美国帮助欧洲防务，结果欧洲人自己就不想搞防务了；美国乐于在联合国分权，结果这个措施也没有给自己带来朋友。<br />
　　<br />
　　甚至连环保主义者都恨美国：Gore 的片里说&quot;We are altering the balance of energy between our planet and the rest of the universe&quot;，我们改变了我们星球和宇宙之间的能量平衡？！如果美国是个传统超级大国，大家可能说美国是法国的威胁，而现在美国居然成了宇宙能量平衡的威胁！<br />
　　<br />
　　作为一个强权，美国的真正问题在于自己的意志不够。美国一个常见体恤衫上写着&quot;These Colors Don't Run&quot;，然而美国从战场逃跑已经不是一次两次了。从越南，从伊朗，从索马里。世界其它国家已经开始怀疑美国的力量。在越南，美军死了5万人才撤退，现在在中东可能只需要5千，也许下一次战争50？或者根本不考虑？<br />
　　<br />
　　整天想着 &quot; exit strategy&quot; 的国家不叫强权。有力量不用，就好像你有一辆法拉利却因为害怕路上有坑洼而不愿开出去一样。<br />
