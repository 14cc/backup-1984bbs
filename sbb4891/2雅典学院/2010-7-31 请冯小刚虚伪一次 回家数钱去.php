刘松萝<br />
<br />
最近，冯小刚执导的《唐山大地震》上映了。<br />
<br />
1976年7月28日唐山地震发生的时候，北京虽然不是灾区，仍然有强烈的震感，有小规模的伤亡和破坏。震后，我们从农村返城时路上的所见所闻，仍然历历在目。以我个人的感受，不希望看到有关唐山地震的影片。看过《唐山大地震》的人说，片子拍得很认真，效果还是不错的。<br />
<br />
唐山大地震是很多人心中永远的伤痛。因此，在影片《唐山大地震》的整个运作过程中，应该多注意一些问题。其中最重要的问题，是钱的问题。<br />
<br />
没有人不准拍电影的人赚钱。现在的问题是，电影人一张口就是钱钱钱。就算纯粹的商品，消费者选择汽车、家电和计算机的时候，考虑的只是质量和价格的问题，不怎么关心生产商赚了多少钱。假如生产商到处吹嘘赚了很多钱，也许还会适得其反。我说过，把电影制作变成纯粹的金钱投入和产出，多少有些厚颜无耻。<br />
<br />
在最近一期，2010年7月19日的《南方周末》上，有对演员陈道明的采访。陈道明说：“那个时候叫拍电影，现在叫抢钱，完全是两个时代。”他还说，“把张艺谋都挤到只能拍‘三枪’这样的电影了，这个社会的价值观还在吗？过去我们文人还讲点风度，还讲点知识，哪怕是虚伪的也好。现在金钱变成主宰，都变成既得利益者了。”<br />
<br />
遗憾的是，在影片《唐山大地震》运作中，对钱还是太重视了。据报道，冯小刚曾给“大地震”5亿票房的预期，并称自己在票房上“独孤求败”，他昨日又称：“就这个数我还是保守估计的。”被问及“刹马镇”导演叫板要“第一个站出来向冯小刚挑战”一事，冯小刚边鼓掌边回应：“太好了，欢迎想挑战的跟我们同天上映。现在他还不明白，等上映完了就明白了。”同时表示自己不会去看“刹马镇”，因为“没工夫”。<br />
<br />
此外，《唐山大地震》还加进了不少植入广告。<br />
<br />
这还不算，闹剧仍然在继续。据报道，有人开始质疑《唐山大地震》的票房数字。为此，冯小刚大放厥词：“有人处心积虑认定‘唐’首日票房虚报，并痛心疾首认为首日不可能超过《阿凡达》，我们的精英们唯恐丢了美国人的脸。怎么可能呢？一定是假的！真对不起伤了你们的心，首日3620是真的，只会低不会高，因为吹牛B要上税的。本民族饱受挫折，养成自悲感，要不国歌里怎么唱起来不愿做奴隶的人们？”&nbsp; &nbsp;&nbsp;&nbsp;<br />
<br />
冯导一向注意票房，是一种敬业的表现。不过在这一次，他真的做过头了。就在新闻视频的下面，一位网友说：“冯小刚你个××，你就知道挣，考虑过别的感受吗。”评论家苏文洋，也写了《当冯导哽咽时他在说什么》。<br />
<br />
其实，专心介绍影片，票房自然就到手了。冯小刚曾经率众为地震死难者默哀，就很不错。在宣传影片《唐山大地震》的时候谈钱，那钱就有可能变成了圣经里面说的血钱。在《马太福音》里，祭司长说：“这是血钱，不可放在库里。”<br />
<br />
冯小刚导演，你能不能虚伪一次，回家数钱去呢？<br />
<br />
