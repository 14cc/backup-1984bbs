																											<div id="postmessage_592023" class="t_msgfont">玉树地震采访期间，我有幸近距离观察了温家宝总理，见证了这位67岁老人在结古镇的40分钟。不为外界所知的是，那天总理高原反应强烈，走路都颤颤巍巍，他拒绝了我的搀扶，但还是被工作人员硬扶进一个帐篷吃药休息了半个小时。<br />
<br />
4月14日，我跟随广东公安边防医疗队一起飞赴玉树灾区。4月15日凌晨3点半到达西宁，下午1点半抵达玉树巴塘机场。因为随同医疗队行动，我成为最早抵达灾区的媒体记者之一。<br />
<br />
怀着迫切的心情，我跟随云南武警边防大队医疗队从机场前往地震重灾区―――玉树结古镇。震后的玉树，到处是废墟，满目疮痍。沿着进城的新建路一路向西，堵塞的交通如同肠梗塞一般，此起彼伏的喇叭声充斥在稀薄的空气中。举目望去，大多数平房被夷为平地，横七竖八的木头椽子裸露在外。而只有三四层高的楼房，大多楼体变形，像被抽掉了筋骨一样，瘫趴在原地，被拧折的钢筋直接戳向天空，面目狰狞。<br />
<br />
在行进过程中，不断有灾民拦住军车，苦苦哀求车上的士兵，请求军人们到他们家里看看。他们的房屋在地震中坍塌了，现在仍然有人被掩埋在废墟，家人用铁锹和双手，无法将亲人挖出来，他们急切希望军队救援。<br />
<br />
4 月15日下午2点半，我到达结古镇。尽管我走得已经不能再慢，但还是出现了气短、胸闷、呼吸困难等高原反应症状。几十名学生家长守候在学校里，他们眼中充满绝望，在苦苦等待着奇迹。18岁女孩孙玉环在废墟旁不断哭泣，她的4名室友就被掩埋在废墟之下。采访到晚上7点半，我决定找地方写稿。<br />
<br />
经过一个小时的缓慢步行，我来到玉树军分区，在各个帐篷间穿行，希望能找个地方落脚。晚上8点27分，在一个最大的帐篷前，我正晕乎乎地埋头休息，突然人群中爆发出掌声与欢呼声，一位老者从一辆中巴下来。当时天色昏暗，我没有看清老人到底是谁。<br />
<br />
老人径直朝穿着军大衣的我走过来，我下意识地伸出了自己的手。老者温暖宽厚的手掌握住了我，连声说：“辛苦了！”我抬头一看才意识到他是温总理。<br />
<br />
“温总理辛苦了！”我反应过来后赶紧说。他满头银发，眉头紧皱，似乎心事重重。总理转身离去，我下意识地跟了上去，陪在他身边。走出一段距离以后，我发现总理身边只剩下我和另外两个工作人员。温总理步伐很慢，甚至走路颤巍巍的，似乎高原反应很严重。我上前扶住温总理，他连连摆手称“没事”。<br />
<br />
“救灾刻不容缓，现在就去开会！”从路边用塑料搭建的临时厕所出来，温总理就急切地往抗震救灾指挥部走去。身边的工作人员连忙拉住温总理，劝他先去帐篷休息片刻。一名男性工作人员介绍，总理下午6点09分到达玉树机场，随即乘车赶往灾区，来不及吃饭，就先后去了结古镇民族商贸城、玉树州民族综合职业学校和玉树州人民医院，以察看灾情和慰问受灾群众。“总理有高原反应，再加上奔波劳累，其实身体很不舒服。”众人反复劝说，硬是将总理扶进了一个小帐篷。<br />
<br />
我跟着总理走进了帐篷。帐篷很普通 ，大 概 不 到1 0平方米，里面摆放了2张小床和1张小桌子。床上的被单被套都是白色的，总理坐在床上大口地喘着气。不忍心打搅总理休息，我连忙退出帐篷外面。工作人员拿来药物，端来一杯热水，让总理服下去。温总理边吃药，边询问救灾进展。休息不到半个小时，温总理就急匆匆地走出帐篷外面，朝会议室走去。晚上11点多，开完会后，温总理又步行到玉树体育场群众安置点看望群众。（ 记者 成希）</div>

							
							
							
							
