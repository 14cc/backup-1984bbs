													<h2>多数决定和少数权利</h2>
						
						
																											<div id="postmessage_447708" class="t_msgfont">从表面看，多数决定与保护个人和少数派权利的原则似乎相互矛盾。但实际上，它们是一对支撑我们所说的民主政体的基础支柱。 <br />
<br />
・&nbsp;&nbsp;多数决定的原则是组织政府和决定公共事务的一种方式，不是导致压制的另一途径。正如没有任何一个自我任命的群体有权压迫其他人一样，多数派，即便是在民主制度下，也不应剥夺少数群体或个人的基本权利和自由。 <br />
<br />
・&nbsp;&nbsp;少数派──无论是以族裔背景、宗教信仰、地理位置或收入水平而论，还是仅因在选举或政治论坛上失利而致──都享有基本人权的保障，这些权利不得被任何政府或任何多数派剥夺，无论它们由选举产生与否。 <br />
<br />
・&nbsp;&nbsp;少数派要相信政府会保护他们的权利和特徵。一旦有了这种信任，这些群体就能够参与本国的民主机制运作，并为之贡献力量。 <br />
<br />
・&nbsp;&nbsp;任何民主政府都必须保护的基本人权包括：言论和表达自由、宗教和信仰自由、合法程序及平等的法律保护，以及组织起来、公开讲话、表达歧见和全面参与社会公共生活的自由。 <br />
<br />
・&nbsp;&nbsp;民主政府认识到，其首要任务之一是保护少数族裔维护文化特徵、社会习俗、个人良知和宗教活动的权利。 <br />
<br />
・&nbsp;&nbsp;民主政府可能面临的最大挑战之一，是接受那些即使不被多数派视为怪异，也令他们感到陌生的族裔和文化群体。但民主政府认识到，多样化可以变成巨大的财富。民主国家视这些特徵、文化和价值观上的差异为一种可以促使自己变得更强大、更充实的挑战；而不是一种威胁。 <br />
<br />
・&nbsp;&nbsp;对如何解决少数派观点和价值观带来的分歧没有单一的答案，可以肯定的只有一点，即只有通过容忍、对话和愿意妥协的民主进程，自由社会才能达成共识，实践多数决定和少数权利这一双重原则。</div>

							
							
							
							
