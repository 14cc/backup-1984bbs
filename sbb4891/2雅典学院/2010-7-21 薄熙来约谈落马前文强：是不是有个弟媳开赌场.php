																											<div id="postmessage_751860" class="t_msgfont">薄熙来约谈落马前文强：是不是有个弟媳开赌场<br />
2010年07月20日 20:08中国新闻周刊<br />
<br />
<br />
“文强（死刑犯），2010.7.7火化，骨灰保存一个月。”据说家人当日下午5点到殡仪馆一个角落的地上领走骨灰，大塑料袋装着，骨灰已凉<br />
<br />
<br />
<br />
（资料图片）<br />
<br />
核心提示：内地《中国新闻周刊》近日刊发题为《重庆的囚徒》的长篇报道，以重庆原司法局局长文强死刑前会见家属的情形作引子，叙述文强的成长及落马历程。报道披露，弟媳谢才萍成为文强最后十年最大的“拖油瓶”。2008年，谢才萍扣留侦办其赌场的警察后逃跑，遭到网上通缉；不久，市委书记薄熙来亲自找到司法局局长文强，问其是不是有个兄弟媳妇在开赌场？再不久，文强落马。<br />
<br />
中国新闻周刊记者/王刚 周华蕾<br />
<br />
【内文导读】<br />
<br />
他跟儿子说，不要恨社会，要恨就恨老爸。不要总上网，少些玩心，多做些事情。你妈妈身体不好，你要给她去送药，我跟政法委商量了，错都在我。<br />
<br />
据黄代强已知的，文强有三个固定情妇：40多岁的沈某，还有30多岁的蒲某和“燕子”。不包括现已辞职的重庆市经侦总队长陈光明，和被强奸的女大学生巫某某。<br />
<br />
儿子终于没有来得及给父亲磕头。下午四点钟，儿子看到父亲的《领取骨灰通知书》：上面写着，文强已于2010年7月7日上午9点15分被执行死刑。<br />
<br />
人物<br />
<br />
文强，四川省巴县人，1.65米，55年出生。生日，阴历1955年12月17日，阳历是1956年元月，多少号连他自己都记不得了，所以一直过的阴历生日。79年入党，80年入警。<br />
<br />
周晓亚，文强的老婆。儿子眼里的守财奴，亲戚都说，过年登门拿得东西少都给脸色的人，在外人眼里是个只进不出的女人。<br />
<br />
文万琴，文强大姐。因为“家里人很老实，不晓得开后门”，知青当了八年，还当了团支部副书记，走不出农村，后来去了中国农业科学院柑橘研究所。<br />
<br />
文伽昊，文强之子。小名，“洪洪”。1981年重庆涨大水，半个月洪水才退，因此得名。胆小，内向。一个从小活在父亲阴影里的人，父亲切断了他跟社会的联系，从小爸爸叮嘱，“家里没有大人在，绝对不要开门”。<br />
<br />
谢才萍，文强弟媳妇。好赌，并因此丢了税务局的饭碗。情夫只有一个，没有媒体此前传的16个。<br />
<br />
陈涛，文强兄弟。重庆市公安局治安总队副总队长。<br />
<br />
黄代强，文强兄弟。重庆市公安局刑警大队副大队长。<br />
<br />
袁元，文强兄弟。早前有一家物流公司，专做长安汽车物流，最近几年在渝通宾馆开了一个纤哥夜总会，后来改名叫A佳国际。<br />
<br />
赵利明，重庆市公安局公共交通治安管理总队原副总队长。<br />
<br />
娃儿，给老汉（爸爸）再磕个头吧……<br />
<br />
文强耷拉着脑袋望着眼前的桌面，若有所思。身后的闪光灯不时地对他闪一下。他穿着一件月白色的衬衣，戴着手铐。在外人看来，今天气色不错。<br />
<br />
从去年8月6日被双规至今，他时常陷入这种沉思。在审讯的时候，在开庭的时候，甚至在这最后的十分钟。有时候审讯人还以为他在回忆案情的细节，但最终从回忆里醒来，他总说，你们有证据，那就按你们说的。<br />
<br />
像他这种人，值得回味的地方很多。在30岁前就在有限的范围内取得了不错的成就，从此以后一切都不免有点走下坡路的味道了。在他16年副局长的生涯中，他仅仅看重于作为一个男人的胜利。没错，这让他每每想起来，连自己都不由得佩服。比如金钱、比如权利、比如那些对手、那些手下和那些女人。<br />
<br />
这种感觉让他发生了潜移默化的改变。不知道从什么时候起，他自我介绍的时候，就省略了头衔，而直接说，我是文强。局长不局长的，在他看来都是无关紧要的形容词。<br />
<br />
这时候，有人挤进法庭，闪光灯闪着让气氛旋即紧张起来，有他的亲人站在对面。<br />
<br />
他似乎依然不觉得，依旧耷拉着脑袋。早上5点10分他就被看守所的民警叫醒，说是带他去见亲人，此前连任何征兆都没有。<br />
<br />
这段日子过得煎熬，二审之后，依旧死刑，复核上报最高院。就像断头铡被拉到了最高处，谁都不知道什么时候会突然落下。有人说，短则3月，长则说不准。也有人说，像他这样的，早该速决。<br />
<br />
但怎么说，也不该是现在。他问过自己的律师，从5月21日二审结束，掰着指头数，三个月也还有一阵子呢。<br />
<br />
昨天晚上文强的大姐和儿子，分别接到通知，说文强想见他们。<br />
<br />
这让他们激动得一晚上都没睡好，第二天早上7点，两人就被拉到了重庆市第五中院。嘱咐能见10分钟，不许谈案情，不许激动，不许高声喧哗，法院有权利终止你们的谈话。<br />
<br />
大姐问，最高院的复核是否下来了？<br />
<br />
对方说，可能。<br />
<br />
到法院的时候，还不到上班时间，里面空空如也。大姐有些起疑，先吃了颗救心丸。不久，沙坪坝的郭局长进来了，说各就各位。<br />
<br />
大姐感觉阵势不对，郭维国是从辽宁锦州过来的，王立军的得力干将。他出马不像是平常的会见。<br />
<br />
但念头仅持续了几秒。他们总觉得死刑是要诏告天下的，白纸黑字的贴在法院门口。如今这么悄悄地安排，肯定是私下的见面。<br />
<br />
快八点的时候，外面喇叭声四起，儿子想父亲要来了。他们被带到了一个大房间，文强戴着手铐坐着，穿着一件月白色的短袖，身边站着两个法警。<br />
<br />
文强还是耷拉着脑袋，看见亲人没有反应，有大约两三分钟的时间，就是闪光灯在那里闪。对着他的儿子、大姐，不断地转换着角度。<br />
<br />
有限的时间里，要说的话太多。<br />
<br />
时间一分一秒的流逝，大姐开始着急了，跺着脚催文强，快说话呀，只有10分钟时间。<br />
<br />
文强如梦初醒，徐徐开口，又像是喃喃自语，之后的几分钟，都是他在说话。<br />
<br />
他说，一点思想准备都没有。今天早上叫我起来，就过来了。我写的一些东西都没有带来，还有判决书，都在看守所。<br />
<br />
还是有好心人，给我送了好多衣服，冬天的，夏天的，都没带来，早知道带给洪洪（儿子小名），可惜了。<br />
<br />
他跟儿子说，不要恨社会，要恨就恨老爸。不要总上网，少些玩心，多做些事情。你妈妈身体不好，你要给她去送药，我跟政法委商量了，错都在我。<br />
<br />
司法局集资建房那个，如果要房子还得添钱，我知道你没钱，你去找我秘书，把钱退给你。<br />
<br />
法院的人说时间到了，再给两分钟。<br />
<br />
这像一道阀门让文强突然激动起来。他带着手铐试图张开了双臂，但他随即惊醒，自己是个囚徒，在儿子肩上，他流着泪说，娃儿，给老汉（爸爸）再磕个头吧……<br />
<br />
这是2010年7月7日，文强的谢幕。<br />
<br />
文家的孩子们<br />
<br />
巴县虎溪曾家镇，文强的故事从这里开始。当年的曾家镇只有一条街，街上有个综合商店，负责着镇子里所有的日用百货。文强的父亲是综合商店的经理，母亲是售货员。<br />
<br />
文强一家七姊妹，文强排行老三。爸妈是综合商店的售货员。七个孩子几乎是隔两年生一个。老大1951年，老二53年，文强55年，一直到老幺63年。<br />
<br />
家里七个孩子，条件自然不好。全村谁都知道文家困难。也由于孩子多，文强只记得自己生日是农历1955年12月17日，至于阳历，谁都忘了。<br />
<br />
文强小时候个子不高，憨笨憨笨的，经常受欺负，父母老是着急他长不高。<br />
<br />
头三个孩子读书都不错。老二和老三文强都当中队长，两兄弟写的文章被当范文，一个很精简；一个喜欢细节，各有特长。<br />
<br />
文强是个在办案中注重细节的人，这恰恰成就了他。审讯张君的时候他说过，你的枪，子弹已经上膛了，但是你忽略了一个细节，枪栓没打开。<br />
<br />
七子妹里，五男，两女。<br />
<br />
五弟兄从小看电影，看到智取华山，就立志要当解放军。课余生活里，他们爱捡起菜籽杆当冲锋枪，在地里边跑边喊，“冲啊，冲啊！”<br />
<br />
1968年，文革开始，停课闹革命，几兄弟都没正儿八经读书。冬天征兵，大兄弟因为球打得好，个子高，率先当兵走了。<br />
<br />
文强由于身高只有1米65，没有当成兵。五弟兄里除了他，都入了部队。<br />
<br />
文强不爱运动，1972年底，初中毕业就去了曾家回龙大队当知青。大队离家3、4里地，说是插队，每天就像上下班一样。大队里都知道文家穷，栽秧割麦，文强都抢在头里，为的是给家里少些负担。赶鸭子下田的苦活他也干。<br />
<br />
因为干活积极，文强当了团支部书记。那时候，回龙大队在整个巴县都属于红旗队，白天下地，晚上理论，干部都是苦出来的。<br />
<br />
回龙大队的人至今记得，文强年轻时不戴眼镜，比现在瘦得多，只是脸相没变，浮肿一如既往。<br />
<br />
社友“幺姆姆”，文强插队时投亲靠友的亲戚家，现在还住回迁房，墙没有粉刷，家徒四壁。她右眼迷离，说起文强总觉得“恁好个人”，“犯错误最多也是五六年前的事”。“上了官家那个船，说不得了”，她又说。<br />
<br />
回龙大队当时有1000多人，九个队，文强当时表现好，100多个知青，就他和另一个入了党。<br />
<br />
文强当了团支书，时不时地在大队开会，当时团支书是积极分子，不愁找对象。不久，队里就传说他和一个女的谈对象。女的叫周晓亚，是虎溪电机厂的子弟，电机厂是个三线企业，子弟中学毕业后，都到回龙大队插队落户。<br />
<br />
当时，文强九队，周晓亚六队。起初，大家都觉得文强找了周晓亚，是农村困难户攀上了工人家庭。不久，情况出现了变动。<br />
<br />
1977年，社会上恢复高考，文强作为大队里的优秀青年，被推荐考泸州公安学校，没想到竟然考上了。<br />
<br />
曾家镇出了个中专生，这在当时是值得炫耀的。况且，从公安校出来，就是吃公务员饭的，文强和周晓亚的关系出现了微妙的变动。当时还在谈恋爱，周晓亚生怕文强走出去就不回来了，追到文强家里去说，文妈妈出来保证，文强将来不变心。<br />
<br />
1980年3月，文强毕业分配到巴县公安局。周晓亚返城，进了虎溪电机厂当工人。本来文强是要留校的，周晓亚怕文强自此两地，坚决不干，要求文强回巴县结婚。<br />
<br />
不久，文强与周晓亚领了证，当时还困难，两人结婚没有办酒。第二年，两口子有了孩子，小名“洪洪”。那一年重庆发大水，半个月才退，因此得名。<br />
<br />
因为年轻，又有警校专业背景，文强很快在基层公安局找到了机会。1983年，全国“严打”开始，文强已经是巴县公安局的“严打”骨干。这期间，文强遇到了“贵人”张文彬。张文彬当时是巴县县委书记。因为严打，文强经常向张文彬汇报工作，张觉得这个年轻人，“能力过硬，作风雷厉，表现十分突出”。<br />
<br />
当时巴县有100多万人口，属于三峡坝区，那几年三峡工程处于风口浪尖，筹建三峡省的传闻也甚嚣尘上，巴县搞机构改革，县委开始组建新的领导班子，不久，文强就一跃成为县委常委，分管政法。<br />
<br />
1985年，还不到30岁的文强升任县委副书记。<br />
<br />
当了县委副书记，文强喊出口号，“干一件事一定干好，家可以不顾”。当年，巴县乡镇企业产值达42714万元，居重庆市第一位，四川省第二位。那时候，在巴县人眼里，巴县是全重庆最好的，以至于直辖后很多年后，老巴县人总跟外人介绍，“巴县是全国最大的县城。”<br />
<br />
这一年底，中共巴县县委宣传部组织“老山”前线战斗功臣作战绩报告，听众达2.9万余人。<br />
<br />
作报告的人里，就有文强最小的弟弟文圆，他参加了对越自卫反击战，结识了4个好战友，在打猫耳洞的时候，死了3个。<br />
<br />
文家七姊妹，五个当兵，一个当了县委副书记，这在当地是惹人羡慕的一大家。曾家镇人人都夸，文强妈生得好，家教好。<br />
<br />
如今，提起文家，曾家镇的村民还都喊得上名字，但言语间却从早先的艳羡，多少有了些唏嘘。<br />
<br />
老三文强，成了天下闻名的贪官。<br />
<br />
老四文健，在部队复原，到了沙坪坝公安局经侦队政委，后来因为文强的牵连，现在被下放去当了校警。<br />
<br />
老六文斌，当兵回来找了谢才萍，自此为一家人引来祸患。<br />
<br />
老幺文圆，复原回来后就精神恍惚了，动不动跟人打架。后来，检查出了精神病，后半辈子待在医院。<br />
<br />
老五妹妹，在虎溪电机厂，属于第一批下岗的工人。<br />
<br />
大姐文万琴，在中国农科院柑橘研究所已经退休。<br />
<br />
大哥文伦，一直在部队。<br />
<br />
进城<br />
<br />
在巴县当县委副书记的那几年，文强在家里的时间少得可怜。当时，正值县城搞小城镇建设，文强负责现场蹲点。<br />
<br />
那几年，巴县在重庆的地位也愈发突出，已经成为全国100个产粮、产肉、财政收入大县之一，其中产肉名列全国之首。<br />
<br />
这期间的1991年，周晓亚和文强闹过离婚。原因是文强忙着顾不上家，周晓亚对文强父母照顾不周。为此，一家人闹得鸡飞狗跳，兄弟姐妹轮着劝。<br />
<br />
周晓亚这个人在亲戚间口碑不佳。在外人眼里，周是个只进不出的女人，过年亲戚登门拜年，拿的东西少，周都会给脸色看。儿子评价，母亲就是个守财奴。<br />
<br />
1992年，文强从巴县县委调任重庆市公安局当副局长。当时，张文彬是重庆市委副书记兼政法委书记。<br />
<br />
办公地点从巴县搬到重庆市解放碑。其时据英国《经济学家》载，“恶魔般的烟囱”正使得重庆成为中国，也许是世界最肮脏的城市。7尺7丈高的人民解放碑，是这座城的制高点。它曾是巴渝人的“精神堡垒”，但现在更是一个飞黄腾达的标记。九县十二区的重庆人都管去解放碑叫：进城。<br />
<br />
文强进城了。<br />
<br />
从一个人口不足百万的县城，到了一个人口近千万的大城市，这对于他也许是忐忑的。1992年，儿子正上四年级，他们从巴县搬到了重庆市大井巷公安局宿舍。文强反复提醒儿子，在城里接触的人更多了，要谨慎。<br />
<br />
这一年，周晓亚也被调到了重庆市天燃气办公室。<br />
<br />
1997年重庆直辖。一个市管着半个省的底盘，政府面临着“小马拉大车”的格局。那时，市委书记张德邻试图从外地抽调3000名干部入渝，但最终没有完成目标，任期便结束。重庆下属40余个区县的3000多名干部获得了升迁。<br />
<br />
文强因此由一名西南城市的局级干部，跃为直辖市的厅级干部，主管着重庆主城区和下辖40多个区县的公安。<br />
<br />
初任副局长的日子里，文强亲临案发现场，深入一线指挥，指挥侦破了一大批有影响的大案要案，多次被公安部记一等功。<br />
<br />
文强面临着来到重庆市公安局的第一次重要晋升。当时市公安局常务副局长人选空缺，而文强能力强、资历深，还在西南师范大学在职研修过两年，在重庆公安系统内拥有最高学历，是这个职位最有力的竞争者。<br />
<br />
但最终，文强没有获得提拔。有人说是他性格张扬，有人说是他野心太大。<br />
<br />
那两年传得最为沸沸扬扬的是，文强和王平的关系。<br />
<br />
1994年，文强认识王平。走得很近，王平结婚他也高调参加了。后来，王平涉黑，开始经营地下赌场，放高利贷。<br />
<br />
有一年，在南山，王平的人与另一伙人打架。据称有市公安的领导到场，当天下雨，后来江湖盛传，是文强给王平撑伞。为此，纪委还专门调查过，最终证明是谣传。<br />
<br />
1996年12月，重庆朝千隧道发生枪战，案涉“二王”（王平、王渝男）。这两人各有一伙人，在市里打得厉害，2000年，重庆市公安局开始捉拿“二王”。2001年，公安部开展第一次全国“打黑”行动，王平成为A级通缉犯。当时文强被公安部找去谈话，讯问他和王平的关系，文强的解释是培养的“特情”。<br />
<br />
王平案之后，文强愈发自信。直至2000年，抓捕悍匪张君把文强推向了自信的顶点。<br />
<br />
当张君被制服。文强打手机向领导报告，说“抓获了”，领导问在哪儿抓获了，文强说：“在我脚下！”<br />
<br />
此后，文强无数次地在镜头跟前重复着“在我脚下！”。他甚至诠释，那是一种武侠小说里才有的情怀，“如果你都看过这些，你就会知道我的这句话是什么。”<br />
<br />
抓获张君那段时间，文强给家里人嘱咐最多的一句话就是，今晚看新闻！<br />
<br />
自此后，文强陷入了自己营造的独特语境中。他周围的人都发现，文强自我介绍的时候，已经省略了头衔，直接就说：我是文强。<br />
<br />
包括2005年破获长寿珠宝抢劫案的时候，面对主犯李忠，上来就说，我就是文强。你现在下去等着我，没有我的命令，不准见任何人……言语间只有他自己。<br />
<br />
张君案后，父亲和儿子有过一段不怎么愉快的对话。<br />
<br />
儿子：老汉你每天上电视，得人民卫士，给好多钱？<br />
<br />
文强：你懂什么，这是钱能比的嘛！<br />
<br />
儿子若有所思，说了句文强也许直到临终才能体会的话，“干得越多，错得越多！”<br />
<br />
“干得越多，错得越多！”<br />
<br />
文强自白：自己担任领导职务以后，工作没少干，提拔总没有机会。甚至“下下级”的干部不断受到提拔重用，心里产生失落感，特别是随着自己年龄增大，提拔机会不大了，就“升官”不成乱用权……<br />
<br />
2000年，文强升任正厅级侦察员，此后10年再无升迁，这让他从此以后一切都不免有了点走下坡路的味道了。<br />
<br />
也是从那个时候开始，儿子每隔一段时间都会在社会上听到父亲出事的消息。有时候还在天涯论坛上看到。儿子去问父亲，爸爸说，我亲手签的死刑都有几十例了，还不说判重刑的，这些事你不可能较真。<br />
<br />
儿子于是习以为常，也愈发坚信，“干得越多，错得越多”。<br />
<br />
也是这个千禧年，爱打牌的税务局公务员弟媳妇谢才萍开始惹祸。这段剪不断的亲缘关系，成了文强最后十年里最大的“拖油瓶”。<br />
<br />
一天晚上，文强回到家里发火，说谢才萍和文斌在外面与和黑社会老大王平开赌场，被举报了，叫老婆周晓亚去问一下谢才萍。<br />
<br />
谢才萍却电话里辩称，我们是正规茶楼还有西餐的。<br />
<br />
而后不久，瞅准文强不在的时候，谢才萍直接来到家里，二话不说扔出两万就走了。<br />
<br />
熟悉文强的人都知道，文强只收熟人的钱，收钱必办事，而周晓亚却是来者不拒。<br />
<br />
之后，谢才萍每到一个地方新开赌场都给周晓亚打电话，希望她吹吹枕边风。作为报答，谢经常给周买买衣服，或者纪梵希的手提包。<br />
<br />
拿了钱，周晓亚曾私底下去跟陈涛和赵利明打过招呼。这两个警察跟文强很熟，周晓亚出面，希望他们更买账些。<br />
<br />
直到2005年11月28日，在观音洞开赌场的谢才萍终于事发。<br />
<br />
谢才萍被抓的那天凌晨2点，文强家里的电话响了。弟弟文斌打来说，谢才萍被抓到渝中分局去了。<br />
<br />
谢才萍的被抓，可以看做上头给文强的一个信号。那两年社会上关于文强弟媳开赌场的举报满天飞，只是文强不当回事。<br />
<br />
中途，周晓亚曾私下去找渝中分局副局长帮忙，结果别人说，帮不上忙。再托赵利民去找这个副局长，对方答，刑事案件，已经进入程序了，没有说情的余地了。<br />
<br />
最终，谢才萍因赌博罪在看守所被关了5个月，还登了报。出来之后，她渝中区税务局的工作也丢了。<br />
<br />
2006年，谢才萍出狱，想给文强电话道歉。文强只对谢才萍说了一句话就把电话扣掉――“你个狗日的把我害惨了！”<br />
<br />
谢才萍的被抓，对文强的政治前途影响极大。当时，文强要提拔当重庆高院院长，组织部都已经找他谈了话，经历此事，提拔最终泡汤。<br />
<br />
文强一度气得想登报，要和文斌谢才萍断绝关系。他在亲戚聚会时见到谢才萍就骂，少给我惹点事！<br />
<br />
没两年，收不了手的谢才萍捅了更大的篓子，这几乎要了“保护伞”文强的命。2008年8月，一名侦查谢才萍赌场的警察，被其手下的“马仔”扣了5个多小时，最后装进麻袋，丢在了赌场几十公里开外的野地。<br />
<br />
谢才萍跑了，成了网上通缉犯。<br />
<br />
不久后，市委书记薄熙来亲自找到司法局局长文强。问，是不是有个兄弟媳妇在开赌场？再不久，文强落马。<br />
<br />
文强在后来的自白书里写道，自己担任领导职务以后，工作没少干，提拔总没有机会。甚至“下下级”的干部不断受到提拔重用，心里产生失落感，特别是随着自己年龄增大，提拔机会不大了，就“升官”不成乱用权……<br />
<br />
致命“兄弟”<br />
<br />
文强自白：对金钱的追求越来越狂热……对极端享乐的追求，把追求花天酒地，极端享乐作为人生的目标，长期我行我素……<br />
<br />
文强最大的质变发生在2006年。那时候，他自知晋升高院院长无望，于是开始整日花天酒地。<br />
<br />
陈涛供述称，2006年开始，他们成了四兄弟，私下按年龄分大小，文强大哥，黄代强二哥，陈涛老三，袁元老四。<br />
<br />
袁元，四十多岁，早前有一家物流公司，专做长安汽车物流，最近几年在渝通宾馆开了一个纤哥夜总会，后来改名叫A佳国际。<br />
<br />
平时，他们喊文强 “老板”或者“大哥”。<br />
<br />
四兄弟一起耍的时候喜欢吃饭，KTV，打牌，一般“斗地主”、“压金花”。自己人打，一般50元的价，一次输赢几千块；跟外面的老板打，就是100元的价，有时500的底，一般有几万块输赢。<br />
<br />
被审讯时，黄代强对文强的评价是，吃酒，唱歌，陪女人，且对女人很大方，从不避讳。<br />
<br />
一直搞刑侦工作的黄代强和文强是2000年认识的。从2005年到2008年，每年春节和文强生日，黄代强都会给周晓亚一万元的红包，此外，还有一块一万二的欧米茄手表。<br />
<br />
文强嗜酒，书架上、酒柜里满是来自世界各地的瓶瓶罐罐。他总对儿子说，留着以后你结婚用。他爱喝 “飞天茅台”，场面上喝，家里也喝，一斤白酒都喝得下，而且不会醉。<br />
<br />
席间，薄酒下肚，文强便摆他的人生经历，说他二十多岁当什么，三十多岁又当什么，最终就说到，张君在我脚下！<br />
<br />
据黄代强已知的，文强有三个固定情妇：40多岁的沈某，还有30多岁的蒲某和“燕子”。不包括现已辞职的重庆市经侦总队长陈光明，和被强奸的巫某某。<br />
<br />
文强私下跟黄说，燕子是某元帅的孙媳妇。但黄代强总觉得燕子像骗子。一次，他托燕子从香港代购LV的男士西服钱夹，后鉴定发现是“歪货”。燕子还打着文强的旗号办事，直接打电话给黄代强让关照进了看守所的谁谁谁，典型的二老板作风。<br />
<br />
也许文强更能从与四兄弟的过往里，找到把张君踩在脚下般的满足感。<br />
<br />
有人回忆，KTV唱歌时，文强一张口，黄代强和陈涛就像维护秩序一样，喊别人安静。还有一次在豪城唱歌，陈涛临时买了一束鲜花，进房后，单膝跪地，把花献给了正在唱歌的文强。<br />
<br />
但跪地献花的陈涛，也是最让文强失望的兄弟。<br />
<br />
2004年，陈涛在江北分局，提拔江北分局副局长失败，文强电陈：陈涛，你是啷个在为人哟？我想提拔起来的人，还从来没有提拔不起来的，你们江北分局的局长、政委跑到朱明国那里坚决反对，你简直是在丢我的脸。<br />
<br />
当年12月，文强就将陈涛调到了市局治安总队一支队当支队长，方便差遣。<br />
<br />
2007年，陈涛想升官，一次性送了文强40万。后来，他被确定为治安总队副队长的拟任人选。当时他在涪陵处理突发事件，收到文强短信，没有字，只有一个“！”他不懂文强的意思，就打过去，文挂了没接，他又发去短信，“大哥，有什么指示，请明示”。过了一会，文强又回了三个“！”<br />
<br />
陈涛晋级成功。<br />
<br />
但不久，文强就和陈涛闹僵了。<br />
<br />
2008年初，市公安局长刘光磊带队去香港学习“处突”，刘的秘书夏小平私下问陈涛，是否经常去劲力酒店吃饭，陈说偶尔，夏小平提点说，你注意点影响。陈涛只把这话告诉了黄代强，但这事还是传到文强耳朵里。文强生气了，之后吃饭，再没叫过陈涛，陈涛去了现场，文强也不让陈敬酒。文强骂陈涛是“叛徒”，不懂得报恩，尔后疏离了。<br />
<br />
文强在警局的兄弟里，赵利民算个另类。赵属于警察系统内少有的附庸风雅的人，别人求人直接送钱，而他除了送钱还送字画。<br />
<br />
2008春节，赵利民给文强送了一幅画。文打开一看落款是张大千。当时就觉得是赝品。周晓亚说，是真的，有鉴定书。<br />
<br />
这幅画之后成为舆论关注的焦点。在一审中，检方认定，文强收受的张大千“青绿山水画”为真迹，价值为364万余元。在二审中，法院邀请国家文物局专家进行了鉴定，专家的鉴定结果是：文强收受的“青绿山水画”为普通赝品，且非高仿品。随后，文强案涉嫌受贿总金额从1625万，降为1211万元。<br />
<br />
赵利民也曾送过马当一幅画，说是韩美林的《马》，后来马当请韩老鉴别，韩说不是他画的。<br />
<br />
四兄弟里，黄代强和文强尤其死党，黄甚至充当“保险库”，代管文强过百万的现金。<br />
<br />
而最终，也是黄代强牵出的王天伦一案，让两弟兄深陷泥潭。<br />
<br />
王天伦老早就在重庆市公安局的涉黑名单里。2003年，他的手下在合川收猪犯了命案。后来上访者闹到了全国打黑办，打黑办批示这是一起家族式的欺行霸市的涉黑组织案。<br />
<br />
王天伦托关系找到黄代强，希望此案照普通命案来办。黄代强为难，说此事帮不了，只有找老板文强或者郭文进。当时，郭文进是市局刑警大队副队长负责1支队，黄代强负责3支队。 前者主办涉黑案件，后者主办命案。<br />
<br />
王天伦清楚此案如按涉黑办，他将凶多吉少。他拿出50个来托关系平事，当时文强收到20个，黄代强收了10个。这个灰色语系里，“个”就是“万”的意思。<br />
<br />
后来的故事是，文强、黄代强摆酒请刑警队长王志勇吃饭，王天伦在场。喝酒中，文强又摆他的人生经历，王志勇不小心插话，惹得文强生气了。<br />
<br />
文强大骂王志勇，你马上要当总队长了，不是我你屁都当不了。<br />
<br />
王志勇没有接话，饭局不尴不尬。<br />
<br />
吃完饭出门，文强又拍着他的肩膀说，你这段时间要低调，不要火爆爆的，组织正在考察你。随即问起王天伦的案子。文强说，“代强想上这个案子”。<br />
<br />
王志勇回去后很窝火，随即把负责该案的郭文进找来，把文强骂他的事说了一遍。问郭文进有没有胆量把这个案子办下去。郭说当然有胆量，只要你敢查下去我就敢查。这件事自此成为文强事发的导火索。<br />
<br />
为了搪塞文强，他们决定把王天伦团伙涉及命案的部分交给黄代强分管的三支队来办理，涉黑这部分继续查。<br />
<br />
第二天，王天伦案到了黄代强手里。不久，黄代强因证据不足命令放人。当时合川县公安局长周安华签字放人的时候手在发抖，说，杀人案都放。<br />
<br />
父与子<br />
<br />
文强自白：我儿子出生后，我那时间工作、教育、开导他的机会很少，学习成绩也不好，心中一直很愧疚……想多找些钱留给他，以满足他今后一生的需求<br />
<br />
被文强无暇他顾的儿子文伽昊，这些年始终活在父亲的影子里。<br />
<br />
他自己都觉得自己胆小，懦弱、内向且无争。一点都没有父亲那样 “张君在我脚下”的气魄。也许父亲年轻的时候和他一样，听文强大姐说，文强小的时候也就这幅样子，是人们把文强捧得失去了自己。<br />
<br />
大姐至今都记得文强小的时候总被人欺负了却不敢吭声。从来都是大姐和大哥帮他出头。这和现如今的文强竟然如此的格格不入。<br />
<br />
经历改变了文强，在和张君的较量中，他从这个悍匪身上学会了生存的法则和胜者为王的逻辑。他曾说，“我跟张君的谈话记录有整整六大本，我退休了要给张君写本书。”<br />
<br />
当年他因为身高不足没能当得了兵，到了后来这一切又遗传到儿子身上。<br />
<br />
2001年，“儿子试图报考市公安学院，身高稍微矮了一点。但当时的主要领导没表态，我没同意他报考，对儿子打击很大，自此一直没有正式工作。”文强在自白中写道。<br />
<br />
文强常年不着家，一回家有一半时间就是在教训儿子，儿子从小对他就很疏离。<br />
<br />
父亲也很少跟他说话，只是在张君落马以后，特地叮嘱：今天晚上你们看新闻。<br />
<br />
那一刻，儿子没有觉得父亲了不起。<br />
<br />
父亲不在家，母亲更是个守财奴。母亲的吝啬有以下证据，给他穿的50%的衣服，都是来自于父亲不穿的，好在爷俩身材也差不多。都说家里有钱，母亲连个保姆也舍不得雇。舅舅给母亲当司机，第一次买了个别克，二手的，开了几年坏了，再买一个竟是东风雪铁龙。<br />
<br />
网络上都说文强的儿子开宝马，家产过亿。儿子文伽昊知道家里有钱，但都被父母藏在了保险柜里。对于他们这种每年传着要被“双规”的家庭，根本不敢露富。<br />
<br />
父母也有着其他的打算。2001年2月份，文伽昊19岁，被父母送去加拿大。可是没多久就又回来了，因为自己不习惯，再说什么都不去了。自此，父亲和儿子闹僵。<br />
<br />
或许父母是为了家庭的未来。但这个孩子偏偏叛逆，唯独觉得重庆好。<br />
<br />
从加拿大回来又进不了公安。儿子想和几个朋友开网吧，父亲不同意，说他分管网吧的。说白了，儿子伤了父亲的心。自此无法沟通。<br />
<br />
过了一段时间，母亲看着儿子在家里不是办法，就让他跟着周红梅去做生意。一下子给了公司35%的股份，还要他做董事长。当了董事长一年没去过公司几回，周红梅晓得他对开公司没兴趣，他也乐得不做事。<br />
<br />
后来公司分红，给了母亲周晓亚一两百万。母亲问红利怎么用，你说了算，儿子说，我还是想开网吧。<br />
<br />
母亲一声叹息。<br />
<br />
直到2008年，文强调到了司法局，不管网吧了，母亲给了儿子30万，让去开网吧。<br />
<br />
调任司法局长后，文强在家的时间多了，没事在跑步机上跑跑步，蹬蹬自行车。有时出门和老下属聚聚，点评一下破不了的“3・19”枪案。和儿子的话也多了，那时候两个人才发觉，不是一类人。<br />
<br />
父亲总说儿子，别上网了，你这样贪耍，不了解社会。<br />
<br />
儿子反驳，还是那句话，“干得越多，错得越多”！<br />
<br />
早在2007年开始，母亲日益感到末日降至。社会上关于父亲的传闻也是越来越多。后来，连父亲自己都怕了。2008年某天，文强接到一个紧急开会的电话，让赶快去，文强临走的时候给老婆说，要是晚上还不回来，你就把家里的钱扔江里吧。<br />
<br />
2009年5月，外面风声很盛都说文强被双规了。有一天，文强喝酒后跟周晓亚说，如果他遭了，去找黄代强要钱，周问要多少？文强说，你找他拿500万。文强还让周去找一个叫徐立君的女人。<br />
<br />
儿子把有些听到了，有些没听到，于他，这都是大人的事情。<br />
<br />
2009年8月，父亲一大早起来说去北京出差。儿子躲在被窝里喔了一声，想不到自此诀别。<br />
<br />
8月7日凌晨3点多，有自称司法局的人来敲门，文伽昊第一反应是打110报警，还以为是社会上的人要来报复。父亲总给他说，这些年得罪了很多人，陌生人敲门不要开。<br />
<br />
来的不是陌生人，是专案组的人。儿子倒放了心，至少专案组的人不会报复。当晚，他和母亲被专案组带走，一同带走的还有他家的“雪梨”――母亲最心疼的那只狗。<br />
<br />
专案组的人给他说，他账户里有数百万的钱，这些他都不知道。<br />
<br />
九个月之后文伽昊从看守所出来。他说，“雪梨”在看守所里待了一个月，瘦得不像样。<br />
<br />
最后的日子<br />
<br />
文强自白：心中有鬼，愧对妻子，想多找些钱回家，以弥补自己的错误……<br />
<br />
从2005年开始，周晓亚就陆续将家里的钱往外转移。最早是2005年4月，周晓亚就给弟弟周泽新打电话，说有些钱要放在他那里。随后他们在解放碑商场买了一个保险柜，能装100万。<br />
<br />
5月，周晓亚给了弟弟一个纸口袋，里面有50万元，让装进保险柜里。之后周晓亚又陆续拿来一些，很快保险柜就满了。2008年初，姐弟俩又在商场买了一个大号的保险柜，这个能装300万。到了2009年5月这个保险柜也满了。<br />
<br />
那段时间，姐姐不断给周泽新打电话说，社会上风声很大，文强要遭，钱放在你那里也不安全，让他转移。于是他们又买了一个能装300万的大保险柜，把钱转移到舅子曾建军家。<br />
<br />
2009年8月7日，凌晨三点多，姐姐周晓亚给周泽新打电话，说她们家门外有几个人敲门。他问姐姐要不要报警。周晓亚说不用，有事等会再说。电话挂断。之后，弟弟不放心去到她家楼下，看见她家灯火通明，还有一个警车停在车库，他明白，文强出事了。<br />
<br />
当天，重庆各大报纸刊登了一则简讯：据中共重庆市纪委有关负责人证实，重庆市司法局局长文强涉嫌严重违纪，目前正在接受组织调查。<br />
<br />
周晓亚想不通的是，专案组给她出示的《立案决定书》里，竟然是文强涉嫌强奸案。<br />
<br />
据重庆市公安局打黑办出具的《破案经过》证实，2009年7月12日，重庆打黑办接到匿名举报，一姓巫女大学生，可能被黑社会老大侵害。第二天，重庆市公安局找到巫某某，她陈述了文强的强奸事实。<br />
<br />
2009年8月7日，文强完成了第一份审讯笔录。他说的最多就是，你们如果有证据，那就按你们说的。<br />
<br />
而当初，他在审讯张君的时候，他挥着手说，现在你坐在我的下方，你是罪有应得……<br />
<br />
尾声<br />
<br />
儿子终于没有来得及给父亲磕头。<br />
<br />
法警把他们拉开了，儿子也不急，他觉得还可以见父亲，至少在死前那一刻。<br />
<br />
但上午11点，他们从朋友的电话里得知文强已经被执行的消息。消息已经上网，当时他们还蒙在鼓里。<br />
<br />
这天，大红色的政治标语久违地包围了重庆的解放碑，“贪官亡，冤魂安，党英明，国昌盛”……<br />
<br />
下午三点，重庆市公安局的人找到文伽昊，说最高院的法官要和你核实几个问题。文伽昊以为是最高院的人，他对着一个操着北京话的人说，我母亲判那么重，我父亲都死了，我母亲还有自首情节，我爸爸说我妈妈可以早些出来。<br />
<br />
第二天，他的谈话被刊发在报纸上。<br />
<br />
下午四点钟，儿子看到父亲的《领取骨灰通知书》：<br />
<br />
上面写着，文强已于2010年7月7日上午9点15分被执行死刑。<br />
<br />
下午五点，九龙坡区西郊路2号，重庆市殡仪馆。<br />
<br />
文强的骨灰装在一个大塑料袋子里，被扔在角落。上面一块白色泡沫写着，文强 （死刑犯）7月7日火化，骨灰保留1个月。旁边一个袋子属于一个无主病人，焚于5月。</div>

							
							
							
							
