																											<div id="postmessage_496820" class="t_msgfont">感谢网警<br />
 <br />
刚刚接到网站管理员通知，说是按有关方面要求――删除了我昨夜刚写的《人民监狱为人民》一文。【当然想阅读的，还是可以继续搜索，老话叫东方不亮西方亮，不可能地球上同时都是夜晚。藏族民歌说：虽然鸟已经飞走，但天空中已经留下翅膀划过的痕迹。】<br />
我知道所谓的上面，就是我那些网警兄弟――而我对他们这个蒙面群体，几乎很少有过怨恨。在不同的警种之中，要说活得最累最苦最廉价又最不招人待见的，就算他们和国保的了。第一，这两种警察没有灰色收入，没办法敲诈网民和异见分子；相反这些人要骂他们，通常情况下，他们还不能公开还嘴。第二，能当网警和国保的，多数都还算是局里稍有文化的人，大学毕业，有一些专业技能和思想水平。这样的人因为命运而来办这种有违良知的差事，内心和精神多少都有一点分裂。我有一些旧日哥们，要养家糊口还在披这身狼皮，偶尔坐下来喝酒聊天，也都骂骂咧咧说自己缺德。<br />
也有陌生网警偶尔给我留言，只说正在看我的文章――我知道，这不是威胁，是在暗示他们也是我的读者，也有区别善恶的能力。如果说他们真要办谁，他才懒得去删帖，留下正好做罪证。说来说去，删帖也是一种保护。他怕其他更为险恶的上面，逼着他们来查办谋逆大案，或者封杀网站或博客，对作者则更是一种伤害。<br />
警察是半军事化部门，一切讲究是要绝对服从。在这个风起云涌的时代，从警多数时候是平民子弟的无奈选择，因为本质上充满罪感和危险。20年前，官宦人家爱把不肖子孙弄去从警，今天则基本绝迹，因为这活儿实在不算是荣耀和富贵的选择。<br />
单拿网警来说，监督网络言论就是他们的职责，也是他们的饭碗。虽然这是一碗违背宪法的饭，但在就业如此困难的今天，他们也只能吃下去。一般来说，上面的上面会给他们的所谓网络“执法”，划定一个大致的杠杠――哪些必须删，哪些必须立案，哪些需要留网查看。但是在具体的执行上，实际多是要看各自的判断力。<br />
言论的罪与非罪，本身就是一个法官都要模糊打架的问题，随便放权给一个年轻的网警来决定，难免不出差错。实际上，面对每天数以百万的网文，这些弟兄真的头疼，也真的难以取舍――在思想深度不够的时候，需要的只是人的天良准则。而稍留天良，在这个高压时代，又确实是一件危险的事情。<br />
网警也是人，也是我们的弟兄子侄所组成。网警比起那些街上的流氓来说，还真可以算读书人，他们每天的阅读量，实在超过很多的知道分子。由于他们重点阅读的多是一些揭示真相的文章，他们在生活中觉悟要远远高于成都金牛区公安局法制科那个著名的徐科长。<br />
最后我还得说，感谢网警。这个时代要是没有这个新的警种，还真不足以在历史上留下一个特殊的印证。否则，儿孙们在考古时非要说这是一个和谐的时代，我还要去另外举证才能反驳。事实上，网警还是为我们留下了一些空间的，如果不是这个群体的良知，他要真的全部封杀，你还真的无地去起诉。<br />
两德合并之后，起诉定罪过一个当年守护柏林墙而开枪杀死过一个翻墙者的士兵。辩方说他只是奉命，但法庭定罪的依据是――奉命开枪无罪，但开枪打死人则有罪。因为生命法则和良知告诉每个人，你开枪的时候，可以抬高一寸。<br />
谢谢网警，谢谢国保，你们每抬高一寸枪管的时候，人性就高一尺，罪恶就远一分。开枪但并不击中目标，是每个人都能做到的，也是上司不能定罪的事情。因为判断不准或瞄不准，是所有人都不能指责的。</div>

							
							
							
							
