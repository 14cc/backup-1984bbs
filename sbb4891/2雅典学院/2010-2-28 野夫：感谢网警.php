													<h2>人民监狱为人民</h2>
						
						
																											<div id="postmessage_497460" class="t_msgfont">作者：野夫<br />
<br />
日期 2009-12-8 9:05:00<br />
<br />
一<br />
<br />
　　十几年前我在武昌监狱服劳役之时，还兼任着队里的“宣鼓”一职。所谓宣鼓，是湖北所有监狱【他省情况不知】至今都还沿用的一个职务，全称叫宣传鼓动，也就是相当于队里的文书，如果在党内得叫宣传委员吧。<br />
<br />
　　宣鼓要负责队里的黑板报【这玩意也是中国特色之一】，以及在墙上书写各种应时应景的标语口号。我的书法放在劳改队，那还是略有可观的；于是有机会被借到其他几个队里去书画高墙。<br />
<br />
　　某日我正搭着架子在草拟口号时，忽然看见远处铁路边上的大标语――人民铁路人民办，人民铁路为人民。会心一笑，于是在墙上也模仿这一修辞，大书了这样两行字，只是把铁路两字换成了监狱。<br />
<br />
　　几个管教来看了，略觉古怪――似乎此前还没有这样的提法，但是也说不出究竟有何不妥。还是教导员【大队级别】有政治水平，过来说不能这样写。我故意愚蠢地问为何，他说监狱就是监狱，还是不要加人民为好，更不能为人民了。我继续傻逼地运用逻辑请教――既然国家叫人民共和国，警察叫人民警察，军队叫人民军队，还有人民检察院，人民法院，人民医院，人民政府，人民等等一切，那为何监狱不能冠名人民呢？<br />
<br />
　　他沉吟半晌，似乎也无法反驳我的逻辑，最后只好说――反正上面还没有这个提法，你不能擅自做主。再说监狱嘛，是处理敌我矛盾的，是为敌人准备的，因此说人民监狱为人民，恐怕不大合适。<br />
<br />
　　我装着理解了，只好重新涂鸦一遍。<br />
<br />
二<br />
<br />
　　这个叫着人民共和国的国家，公检法都要加个人民的冠词，唯独劳改局【现在改名叫监狱管理局】不能加人民，确实不合逻辑。如果用接近事实的说法来看的话，前面那几家似乎可以不加，至于监狱嘛，这个那确实可以加。<br />
<br />
　　人民这个词，在流氓汉语中，是一个政治定性的代词。原本指称一切被统治者的这个词，突然在本朝本末倒置变成了一种特有的政治身份，想来真是咄咄怪事。某公民假设某天被开除出人民这个阵营，那就是敌人，那就得蹲大狱。<br />
<br />
　　但凡叫“人民民主”【比如朝鲜】或者“人民共和”的国家，一般来说，都与实际的人民没什么关系。那些确实由人民授权的国家，他又特别不愿冠以这样的褒义称谓。假设哪天奥巴马称呼美利坚人民共和国，那真正的美国人民肯定要感到恐惧，要把他赶下台。<br />
<br />
　　前几年好像是贺卫方等法学家提议――法院就是法院，去掉人民两个字。本来中规中矩的提议，在本国那就要震惊朝野了。其实，硬生生组装上人民二字的法院，不仅不是由陪审员来裁定罪与非罪，反而更加显得这样的法院很不专业。似乎它不是由一批法律专家组成的审判机构，而是由一个不负责任的名词在那里定夺生杀。<br />
<br />
　　也因此，河北省的人民法院冤杀了聂树斌，现在即便真凶出面自首了，那对他――这个真正无辜被错杀的人民――至今还是难以被伸冤改判。一个法院要黑到什么程度，那才敢叫人民法院啊？草菅人命了，至今不改，不追究全部冠以人民名称的当地公检法的责任，这就是我们这个自称人民共和的国家吗？怎么看怎么都像是只有旧社会，才可能这样暗无天日啊。<br />
<br />
三<br />
<br />
　　人民究竟是由哪些人组成的呢？在本国如此重要的一个政治词汇，究竟有没有一个法律性解释？全国人民代表大会既然是宪法虚拟的一个最高权力机构，那至少也应该对这个词，加注一个权威的内涵外延吧。<br />
<br />
　　比如持有中国护照或者身份证的个体，算不算人民？应该算吧。判刑的人因为没收了护照身份证，当然不算人民了――但是只要没剥夺政治权力的犯人，又还要他们参加所谓的地方人大代表选举――既然还有选举权，那就也应该被视作人民的吧。<br />
<br />
　　犯人选举人大代表――这是我在本国见到的民主特色之一。监狱外的人大代表候选人要想拉票，一般来说还不太容易找那么大批的人画圈。但是在监狱，犯人都是成千上万的，而且说一不二。也因此，我所在的那个监狱长，也就年年当选地方人大代表了。这样的民主，充分体现的是“罪犯做主”――全体犯人投票给监狱长，大家吃肉一次，何乐而不为？回头来看，我们的所有选举，本质上不都是在选监狱长吗？<br />
<br />
　　问题是即便持有中国护照身份证，还是不一定能保证你就是人民。人民共和的国家，还是可以把你驱逐出去，或者趁你不小心出去了就不让你回来。这个祖国像一个脾气乖戾的监狱长，要选票的时候就给你改善伙食，要你给他画押；看你不顺眼了，就关你禁闭或者赶你出门。当然犯人多是愿意被赶出门的，不过，有的人民却不喜欢这样，宁肯被关禁闭也偏要回来。最近上海有个冯律师，就是这样被开除了人民资格的；至今还在日本机场滞留着，全世界人民都在参观这一盛世奇迹。大国崛起的好处就是――老子就是敢把人民活生生地抬起来塞进别国的飞机，你不把我国人民带走我就不让你起航。<br />
<br />
　　那些曾经崛起现在不那么挺然翘然的国家，闲话终归是闲话，你还真拿老子们的祖国没辙。这样强蛮的祖国，不让冯律师们爱还真不行，于是他还偏要想回来，想继续享有他当人民的权利和苦难。<br />
<br />
四<br />
<br />
　　在我朝，医院多叫人民医院，但精神病院却似乎不能叫人民精神病院。叫人民医院的虽然以宰人民为主，而不叫人民精神病院的却是处处为人民服务――就像不叫人民监狱的地方，往往免费照顾人民一样――今天的精神病院也常常免费治疗正常的人民，直到把你治成真正的错乱为止。<br />
<br />
　　在今天的基层县治，为了确保维稳，一般都会申请建设一个看守所，一个精神病院，再加一个殡仪馆。对于那些迷信朝纲喜欢上访的刁民来说，这三个地方都是用得着的。<br />
<br />
　　根据某些特孙子的专家教授的建议，把访民弄进精神病院，是基层人民政府的一个重要慈善行为。野蛮的地方是直接从首都抓回去治疗，文明的地方采取的则是比较和谐的劝进方式。<br />
<br />
　　我曾经听一个政法委书记的经验交流，十分有趣。他说一旦见到老访民来伸冤，他必热情上前问最近看上去身体不大好吧，对方一般都会答是啊。他又说似乎精神状态也不大好吧？对方往往也上钩说――是啊，那个问题不解决，精神当然不好啊。他又说那该治疗还是要吃药嘛，对方往往生气地说没钱治疗。这时他必热情地说，有病治病，政府出钱帮你把身体治好再说。这样，你签个字吧。于是多数时候对方也就签字了，结果被合理合法地送进了精神病院。<br />
<br />
　　把不听话的人民弄进监狱，把信奉党中央的访民弄进精神病院――这是我们祖国目前维持的一种盛世和谐以及稳定。成都前几年评选的一个优秀公民谭君作人先生，因为号召在某个日子去血站献血，也被以颠覆罪的名义弄进了班房。还有京都的许君，也是因为类似的慈善行为，至今都算是取保候审。可见在祖国，做一个好人的风险，实在比做一个坏人还要大许多。<br />
<br />
五<br />
<br />
　　在官方的词典和实际操作中，只有装聋作哑的顺民才被看做是人民。只要人民顺服地适应了墨吏集团的分赃盛宴，那就是中华的盛世，也就算共同和谐的共和国了。也因此，国号似乎叫中华顺民和谐国，可能更接近事实。<br />
<br />
　　关于本国的制度定性，写进宪法的是人民民主专政――在专政这个完全等同独裁的名词前，再冠以人民和民主这两个妖魔化的词汇，实在是既让人糊涂，又令人怵目惊心。早些年原本叫无产阶级专政，后来发现再怎么装也是有巨产阶级在专政，于是改为人民民主这个定语――在民主之前还要叠床架屋地组装上人民这个词，逻辑上是为了区别黑帮社会的民主模式。但根据真实状况来看，窃以为还不如像监狱那样，干脆不用人民二字，显得更为得体一些。<br />
<br />
　　我国人民因为已经充分享有了人民公安人民检察院和人民法院的福利，还有人民警察和人民军队的看顾，于是监狱就不再命名为人民监狱了。否则，所有的专政机关都在伺候着人民，那我们的人民得有多大的福分啊。<br />
<br />
　　若干年之后，我想起当年那个教导员面对我冒昧进呈的那个先进口号时的困惑，还是忍不住哑笑。很多时候我发现，凡是揭示了社会实相的词语，一般是不会采用的。反而是那些完全虚假的称谓，却可以恬不知耻地放肆使用。<br />
