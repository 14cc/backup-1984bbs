2007年7月22日<br />
<br />
&nbsp; &nbsp; 今天不知道胡锦涛的人，和20多年前知道他的人差不多。<br />
<br />
&nbsp; &nbsp; 当时他是团中央第一书记，不过，他在社会上的知名度不如在我们办公室的知名度大。我当时在中央宣传部理论局工作，胡锦涛的连襟王彦峰和我一个办公室，王彦峰提到他的妹夫时，嘴角并不总是向上，我们知道这是连襟之间的竞争心理在起作用：既自豪，又不服。<br />
<br />
&nbsp; &nbsp; 1985年下半年，原贵州省委书记朱厚泽调任京官，胡锦涛督抚贵州。1987年1月，胡耀邦禅位，胡锦涛受调查。调查结果出乎所有人意料：“贵州省的广大干部和群众反映，胡锦涛同志是解放以来本省最好的省委书记之一。”有人想，既然这么好，就让他到西藏锻炼锻炼，替他老师擦擦屁股吧。是年，胡锦涛领西藏。<br />
<br />
&nbsp; &nbsp; 1997年11月某日，朋友罗海纲来喝茶，推盏之间，谈到旧日同学，他说，他的一个大学同学曾经在团中央工作，做胡锦涛秘书。当时，他有一次打电话找同学，不在，接电话的是另一个人。<br />
<br />
&nbsp; &nbsp; “他不在，你要留话吗？”那个人温和地问。<br />
<br />
&nbsp; &nbsp; “你是谁呀？”海纲问。<br />
<br />
&nbsp; &nbsp; “我是锦涛，”语调亲切平和。<br />
<br />
&nbsp; &nbsp; 但海纲没有听清，大声问道：“谁？”<br />
<br />
&nbsp; &nbsp; “我是锦涛，”音调没有改变，依旧平和，依然亲切。<br />
<br />
&nbsp; &nbsp; 可是海纲还是没有听明白，有点不耐烦地高声喊道：“大声点行吗？”<br />
<br />
&nbsp; &nbsp; “我是锦涛，”声音并没有提高，只是吐字清晰了些，依旧平和，依然亲切。<br />
<br />
&nbsp; &nbsp; 海纲听清楚了，赶忙挂了电话。<br />
<br />
&nbsp; &nbsp; “我当时没有想到是他，以为是另一个秘书，他的修养真好呀，”海纲喝了一口大红袍，从口中吐出一息清香说。<br />
<br />
