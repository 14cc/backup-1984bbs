“非暴力”：非常危险的武器--重读《甘地自传》之三 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;印度另一个政治领袖真纳，就赞成另一条思路。真纳的气质风格和甘地完全相反。他受到极好的教育，思维敏捷，是一个极成功的律师，也是一个宪政主义者和立法人。在历史变化中的英国殖民政府，面对百年复杂局面，法治状态也有过多次反复。真纳也曾在法治倒退的低谷时刻，愤而退出立法议会表示抗议。可是他的基本态度是个法律人，始终反对甘地以群众运动对抗不合理法律的做法。他公开批评甘地的群众运动到哪里，哪里就开始混乱。他不赞成甘地经常以发动成万成万的民众主动违法入狱的做法。他反对甘地的非暴力抵制，这些抵制包括拒绝纳税；拒绝服兵役；印度公务员全部退出公职；印度法律工作者退出工作，抵制法庭；学生全部离开公立学校；焚烧洋货和抵制英国纺织品等等。在甘地号召下，青年学生成为非暴力运动主力，纷纷退学，“杀向社会”。在这次运动中，有3千名研究生、6千多名大学生和4万多名中学生退学。不但暴力冲突和死亡不断，民众连续挑战法律、自动入狱，“以前被社会认为是不光彩的入狱，现在被看作是爱国主义的最高奖赏。”<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;作为国大党创始人，真纳认为甘地来了之后，改变了国大党成立的初衷。在独立前，逐步扩大自治、大幅增加印度人政府公务员、建全法治、发展现代教育，曾经是印度知识精英长期在努力的目标；而现在在非暴力不合作的口号下，在制度建设和法治建设上倒行逆施；最终伤害的可能是印度的长远利益和民众利益。例如，后来的印度女总理、当时还是小女孩的英。甘地就因为离开学校教育，由家里聘请家庭教师读书。可是，并非每一个退学的印度孩子能够做到这样。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;如甘地对泰戈尔说的，他“日以继夜，绞尽脑汁”，只是要找一个再次发起非暴力运动的突破口。反盐税就是他找到的突破口，只是逼走英国人的一个手段，而不是就盐税而论盐税，目标单纯为改善民生。采用长期、持续不断进行非暴力群众运动，是个紧逼盯人的策略。因为不论怎样对方都是输家。它的目标是快速的根本变革。在印度当时的情况下，是以支付正在行进中的法治和制度建设为代价的。印度民众本来就薄弱的法治观念也被毁坏殆尽。真纳在给甘地的信中说：“你的方式已经在迄今为止你所接近的几乎每个组织中，以及在国家的公共生活中，引起分裂和不和，全国民众都在铤而走险，所有这一切都意味着彻底的无组织和无秩序，其后果如何，我焦虑地注视着。”<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;在甘地、真纳不同态度的背后，还隐藏着一个古代社会如何应对现代化全球化的问题。英治印度从古代社会进入现代的同时，英国本身也经历了工业革命，先发工业国家向滞后的农业国大量收购原料，反过来倾销成品，也是无法避免的历史现实。对于生产力处于古代水平、自给自足的印度，必然带来巨大冲击。例如，在印度的一个突出问题就是土布和洋布的转化。英国突然发展了纺织工业，就收购大量棉花、反销廉价的机织洋布，使得印度成千上万的土布织工突然失业限于绝境。究竟是积极应对“转型”，还是坚持古典生活、反抗被纳入近代化轨道，是一个决定命运的选择。这也是甘地和真纳们的分歧所在。问题不是人最终是否应该回归自然的哲理讨论，问题是：在印度当时的时间、条件、地点之下，究竟什么是历史的必然。甘地自己从此一身土布“拖地”，要求全国民众回归土布手摇纺车时代，烧尽价廉物美的洋布，要求国大党人必须以每日亲自纺纱若干作为党费交纳。最后实在无法推行，不了了之。而真纳对这种勉强拖住历史车轮的观念和做法，不屑一顾。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;甘地在当时就风靡全球，包括风靡英国。它的历史背景是：包含英国在内，强国对殖民历史本身普遍的反省和道德谴责。甘地大量反暴力哲人格言，展现的对民众的号召力，一身“拖地”纺纱和东方式神秘，不断入狱绝食的政治道德形象，使他成为世界偶像。很少有人去想，在将类宗教追求，转化为和法治逆向的实现政治诉求的手段，在一个两亿多低教育人口的大国付诸实践，必定包藏隐患。而甘地的非暴力不仅在实践中出现种种问题，在理论上也走向极端，逻辑混乱，令人困扰。 1936年8月，甘地曾经接见中国国民党元老戴季陶，表示中国在进行抗日战争，便说明是没有非暴力主张。不久甘地接见世界基督教领袖，再次提到：“不管怎么说，中国不是在实行非暴力。它能英勇对日抗战，说明中国从来没有非暴力意愿。说它只是自卫，从非暴力原则来说，这不是理由。”“从非暴力主义者的立场来看，我必须说，以一个拥有四亿人口的中国，来对付一个开化了的日本，还是不得不以日本人的同样手段来抵抗日本侵略，我以为这是不适当的。假如中国人有我这样的非暴力信念，就不需要和日本人一样的最新毁灭手段。中国人可以告诉日本人，‘带着你们的毁灭手段来吧，我们以两亿人给你，可是剩下的两亿人我们是不会屈服的。’假如中国人真的这样做了，日本人就会变成中国人的奴隶。”<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;作为个人来说，任何作为个人立场的和平主义信念，都是必须得到尊重的。二战期间，英美都有甄别“基于宗教信仰的和平主义者”的机构，得到确认的公民可以免服兵役，以保护宗教自由。甘地不一样，他的非暴力不仅是个人信念，还是要广泛发动的群众运动。他有关战争的理论和信念，要扩大向全民宣扬，要发动整个印度民众抵制协助盟军参战。英国的印度总督接见了甘地，表示尊重他的个人信念，却希望他不要在反法西斯战争的紧要关头煽动民众反战，也不要动摇作为盟军一部分的印度军队的军心。在战时，这样的煽动是违法的。甘地不顾二次大战中世界危急局势，以及战时状况的特殊，反而认为，是否容许公开宣扬，是在捍卫“言论自由 ”。而“言论自由以及相关行为，是民族生活中不可或缺的。而宣言以非暴力替代战争，正是拯救西欧免遭屠杀的福音。”<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;总督两度与甘地会谈，总督认为，甘地作为个人并非没有言论自由：“你是彻底反对履行作战义务，并且已经公开表达过了。”总督说，“但是我不能让你向别人，向战士或军工厂工人，进行反战宣传，削弱民心士气。”总督告诉甘地，在二战危急关头“说是反战而不危及印度利益，是不可能的。这个利益也包括了你们要求的言论自由。”总督的意思很简单，假如纳粹和日本军国主义获胜，印度的利益和自由都将不复存在。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;而甘地仍然坚持己见，发动了14个月的非暴力反战宣传运动，有两万五千人因此陆续被捕，也很快尽数释放。不久，德军攻入苏联，然后珍珠港袭击事件爆发，日本投入太平洋战争。争取反战之“言论自由”的非暴力运动在印度也越来越不得人心，难以为继，最后草草收场。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;国大党一些领导人和甘地想法不同，他们一直希望借二次大战的机会，以战争配合来作有关快速独立的政治交易。多年来，印度的自治权一直在不断扩大和加强，此时英国政府承诺印度再次立宪，印度作为自治领，建立自己完全的民选政府。可是鉴于战争局势，在此之前，仍由原来政府负责国防事务，使印度成为整个反法西斯战局的一员。实际上，此刻的印度政府已经相当“印度化”了。而甘地要求的是“立即完全”的独立，由印度立即接管国防和军队。那是1942年，不仅中国，整个东南亚烽火连绵，这样的条件是英国政府不可能接受的。在甘地起草的国大党决议中，说立即独立方案的挫败，“已经在印度迅速引起广泛的仇英情绪，并且满意日本的军事胜利。”而假如满足国大党要求英国立即撤出，“本党可以将仇英情绪转为友好。”指出假如不满足这个要求，“混乱局势可能蔓延”，这显然是一个威胁的说法，也对自己掌控民众情绪的能力十分自信。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;就在二战局势最紧张的关头，甘地“积二十二年斗争经验，动员所有的力量”，发动领导了最后一次大规模的非暴力运动。印度政府以总督名义发表了一个国务会议的决议：“国大党正在从事某种非法活动的准备，甚至已发生暴力事件，破坏通讯和公共事业，煽动罢工，妨碍公务，阻碍政务，政府曾对此容忍，希望自行修正而无效果。对此挑衅，政府被迫起而应对。”此时若“采纳国大党要求，印度会立即变成无政府状态，使印度为人类自由之共同目标所作的努力，全部失败。”随后，甘地和他的秘书被逮捕软禁，家属可随行照顾生活。七天后甘地秘书心脏病突发而死。他的死亡引发民众猜忌，立即爆发全国流血暴动，攻打警局，暴乱中各地也发生警察开枪，全国死亡超过900人。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;二战结束，英国人开始履行撤出的承诺。甘地带领的一次次“非暴力运动”，因其轰轰烈烈，成为独立进程中最抢眼的标志。其实，这是一个复杂推动的历史结果。有双方无数政治家的努力，也有双方的历史推进。英国从东印度公司开始，到后来女王下诏，直至离开，有两三百年的过程，英国本身也在经历惊人变化，包括英国对各殖民地的看法和做法，都有本质的改变。香港就是一个明显的例子。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;甘地说过，“假定有一种人，决心不屈服于暴君的意志，暴君就会感觉自己的恐怖手段无效了。如果有足够的食物去填塞暴君的嘴，终有一天他会感觉不消化。假如世界上所以的老鼠都开会，决定它们都不再怕猫，大家跑进猫嘴，那时老鼠就都能活命了。”这是甘地一个极端的说法。至少很多人相信，印度独立是甘地的非暴力敢死队挑战暴君胜利的单方面结果。实际上，胜利的也是英国的政治和制度进步。三百年来，英国从一个老牌殖民者，变为二战中民主进步力量的代表之一。英国在印度的殖民政府，在漫长的时期里，面对历史现实，它不可能在印度民族独立意识觉醒的时刻，就立即放弃。英国人在三亿人口的印度次大陆，本身人数非常少，在民众起来暴动的过程中，多次有过政府失控的流血事件，尤其是在早期。可是，这并不说明，英国政府是和希特勒政府一样的暴君。尤其是后期的印度政府，已经是大半由印人自治的政府了。 <br />
 <br />
&nbsp; &nbsp;&nbsp;&nbsp;英国人撤出印度的计划，其实很早就基本定下来。只是具体怎么做，是作为英联邦中一个自治领还是彻底独立，如何定位一直是自治领的五百多个小邦国，都需要双方政治家反复协商。在离开之前，英国人尽其所能为各方协调，希望得出一个满意结果。长期以来，印度群众运动导致民众类宗教的政治热情却高烧不退，隐含着极大的危险，一个标志就是宗教冲突绵延不断。英国人曾经多次警告，他们过早撤离，可能会出现骚乱和动荡的局面。可是印度政治家们摩拳擦掌，恨不能马上彻底接下这个国家。在外部压力事实已经撤出的时候，内部宗教冲突激化，印度已经像个火药桶。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;甘地引发非暴力运动的具体诉求，只是被看作一个“由头”，“哈里发运动”也是如此。自古以来世界伊斯兰帝国实行政教合一的哈里发制度，最终在1924年由土耳其的凯末尔废除。在此之前，为保存哈里发制度，印度的伊斯兰社区，要求英国和土耳其签订合约时保障传统的哈里发地位。甘地动员印度教民众支持和自己宗教完全没有关系的“哈里发运动”。相反，作为穆斯林的真纳，却和“哈里发委员会”保持距离，也反对甘地的做法。甘地的非暴力反英运动纳入宗教诉求之后，后果可想而知。最后引发的暴力不可收拾。甘地叫停后，印度教徒很容易停下来，哈里发对他们来说，本来就是莫名其妙毫不相干的事情。而情绪被煽到最高潮的伊斯兰群体，顿时觉得自己被印度教徒出卖。此后几年，印度教和伊斯兰教发生几十次大规模的暴力冲突，大量死伤，这些冲突又催生了双方的宗教极端组织。伊斯兰教徒占印度人口的四分之一，很长时间内，穆斯林联盟的诉求是他们作为一个独立政党，和国大党平等，而国大党更多时间表现出来的是一党独大的傲慢：要求对方解散，成员加入本党，然后我分一定比例席位给你们。 <br />
 <br />
&nbsp; &nbsp;&nbsp;&nbsp;在英国人离开之前的宗教和政治冲突，是印度已经高度政治自治的结果。而甘地和国大党都曾表现出对少数社群的忽略与不尊重。各省立法议会开会，首先演奏违反伊斯兰教的“民族歌曲”，国大党管辖的各省，学校课本内容都是对印度教的宣言。穆斯林的呼唤祈祷被禁止，清真寺的礼拜被袭击。甚至发生印度教徒作伪证，诬陷整个穆斯林村庄所有村民都参与谋杀。在作为少数群体的穆斯林没有安全感的时候，国大党又不愿意和他们的代表合理分享政治权力。 <br />
 <br />
&nbsp; &nbsp;&nbsp;&nbsp;穆斯林政治精英在长时间里，一直坚持要维持一个完整的印度。可是终于失望，萌生去意，要求成立自己的巴基斯坦国。政治谈判不成，从穆斯林联盟号召“直接行动”的抗议开始，直至后来分治期间的暴民互相残杀。印度沦为自相残杀的杀戮场。在甘地领导印度独立运动的四十多年里，双方失控，导致警民共将近八千人死亡，在英国人撤离的一年之内，印人自相残杀约达一百万人（在写《我也有一个梦想》时，我曾简单提到这一段历史。我当时采用了我见到的最小数字，死亡四十万人。后来查阅不同资料，大多认为没有准确数字，历史学家们估计在死亡在一百万人和两百万人之间）。最后，甘地以他的“圣雄”声望，绝食平息骚乱，创造了最后一个奇迹。 <br />
 <br />
&nbsp; &nbsp;&nbsp;&nbsp;我想，一年如此杀下来，也该罢手了。而甘地本人最后被印度教极端分子刺杀，如求仁得仁，给一个非凡人物一生追求的事业，画下一个圆满的句点。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;此后，各国各地成功的非暴力抵抗，都加在甘地的功劳簿上，也就很少有人再问：在一个非暴力的故乡，在非暴力的理论和实践盛行四十多年后，英人撤离了，为什么会充满如此的暴戾血腥之气。 <br />
 <br />
&nbsp; &nbsp;&nbsp;&nbsp;非暴力是否成功，不是单方条件能够决定的。挑战一个有合理价值观的政府会产生作用，对日本军国政府、希特勒的纳粹政府是无效的，更不要说以非暴力反侵略了。其次，作为一个政治手段，非暴力抗议不是一个简单的概念，号召者并不能因为“非暴力”三个字就占据一个永远不错的道德高位和自诩“政治正确”，不能因此就可以不服任何责任。在预见对方可能使用暴力，可能导致大量民众流血牺牲的时候，领导者是应该有所顾忌的。非暴力抗议和民众的素质也有关，今天高度文明、高度民主和法治健全的国家，民众对政府某个做法不满意，决定大家有时间限制、有条件限制地抗议表达，这是一种非暴力抗议方式；而在一个社会条件不成熟，民众自控能力差，法治不健全的社会，要以大规模、无休无止的、具有挑战性、不达目的誓不收兵的非暴力运动，又是另外一回事。后者很可能引发暴力流血的后果来。这和它的对象有关，和要达到的目标、方式、时机等等都有关。总之，非暴力概念被引入政治领域后，是一个非常危险的武器，也是一个非常复杂的课题。甘地的非暴力绝非和平概念，那是时时提到死亡和献身、介于和平与暴力之间的一种政治手段。 <br />
