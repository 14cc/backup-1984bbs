													<h2>非暴力：非常危险的武器</h2>
						
						
																											<div id="postmessage_635540" class="t_msgfont">原标题：重读《甘地自传》&nbsp; &nbsp;&nbsp; &nbsp;<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;作者：林达<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;带着热烈宗教情感的社会改革者―――重读《甘地自传》之一 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;我想自己和大多数人一样，最早遇见甘地，遇到的是一个有定语的名字：“圣雄”甘地，还有他“非暴力之父”的声名。后来，我又发现，自己和大多数人一样，对圣雄甘地其实耳熟而并不能“详”，许多人在景仰宣扬了甘地很久，可能也不清楚甘地的“非暴力”落实到具体，究竟是些什么主张，也并不清楚，在印度独立的历史上，它究竟在发挥怎样的作用。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;说起来有点不好意思，我第一次对甘地事迹有些了解，不但很晚，而且是通过一个故事片，Richard Attenborough执导的《甘地》，它在1982年获得11项奥斯卡金像奖题名、最终获得包括最佳影片、最佳导演、最佳男主角等8个奖项。如此得奖的传记大片，一个特点就是对史实会相当考究不敢掉以轻心，所以从大众普及角度，这确是普通人了解一个历史人物和一段历史的捷径。电影是一种很煽情手段，一般来说，被电影煽过一煽，会对你喜欢的人物更喜欢，对你尊重的人物更尊重。所以那次看电影的经验着实让我吃了一惊，我可以说是带着景仰走进去，满怀狐疑走出来。从此提起甘地，我条件反射般的反应是问号。要消除这些问号，还是要看书。于是在去年我看了甘地自传《Mahatma Gandhi－－His Own Story》，然后匆匆忙忙在年底去了一次印度。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;&nbsp; &nbsp;在印度旅行45天，最后在德里有一天空闲时间。我也记得德里应该有个甘地纪念园，最后没有去，心里也没有太大遗憾：在印度，到处都是甘地塑像，遇到的一打没有，七八个不止了。甘地事迹是印度学校对孩子们最正面、地位不容动摇的教育。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;近年来印度学界研究的“复杂甘地”逐渐向民间扩散。一个例证是印度到迪拜的飞机上，就在播放刚刚上映4个月的新电影：《我的父亲甘地》。虽然这不是一个精心制作的大片，可它是个货真价实的“印度电影”－－由印度导演Feroz Abbas Khan执导，由印度宝莱坞明星Anil Kapoor制片，通过讲述甘地大儿子失败的人生故事，折射了甘地作为人的个性另一面：在家里的专制，剥夺孩子们上学的机会，给他们的人生带来极大困扰。虽然这无损圣雄光环，可是一个民间解构，对印度也并不寻常。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;曾经困扰我的，是在非暴力运动中看到预期发生的暴力。这让我想有更多的了解，甘地的非暴力究竟是什么？对甘地本人是怎样的概念，它怎样引向印度独立的操作，又引出什么后果？这是我想读一些书的原因。从印度回来我又读了一遍甘地自传，查了一些不同的甘地传记，还有一些印度历史。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;甘地自传可以让读者理解他成长的环境背景、以及获得他个性形成的第一手资料。甘地于1869年出生在印度一个西部半岛的小邦国Porbandar，人口7 万左右，首都人口1万5。甘地家三代都当过几个类似的小小邦国的总理，当时印度遍布这样的小邦国，直到印度独立时还有5百多个。这些小邦国的日常管理高度自治。我在另外一本传记中读到，甘地祖父曾经和Porbandar的摄政王政见不合，宫廷卫兵在他家门口架起大炮，一炮就把他轰走了。这些复杂的邦国政治宫廷争端，英国人是根本不管的。早年生活在这样的小邦国，感受不到什么多少殖民气息，却能够沉浸在浓浓的本土宗教氛围中。尤其是母亲对甘地影响很深，她不仅恪守斋日，还经常额外给自己整日和数日禁食。禁食对于甘地是一种带有强烈宗教意味的行为。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;甘地自小是个极敏感和自卑的孩子。在少年和青年时期，甘地的学业事业都不顺利，打击特别大的是进大学后第一年就读不下去，只好辍学。同时，他却有着三代小邦国总理所形成的家族压力和自我期许。一个长辈告诉他，时代不同了，按照过去模式，他已经不可能子承父业；假如他仍然有此雄心，就必须接受现代教育。遂建议他赴英留学。甘地因此在1887年离家去英国。他的目的是取得律师资格，却似乎不是尽量扩展自己的知识。当时英国考律师非常容易，有的学生突击几个月就可以通过，有些应付考试的参考书甚至可以带进考场。但另有规定，必须上课三年。甘地也就在英国住了三年，他花了很多时间和兴趣在素食协会活动和对于不同宗教的探究上。1891年6月10日，甘地顺利通过考试、取得律师资格，却并没有大学毕业。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;英国这个律师考照制度的特点是：取得执照容易，而要在法庭站得住脚、赢得顾客却很难。它的淘汰环节是在后面的开业执业阶段。甘地取得执照后归心如箭，马上启程回国。三年过去了，他拿着执照却发现自己读书不多、实际并不具备在律师界执业的知识和能力；再加上个性羞怯，困难重重。他一开始试着在孟买开业，遇到并不难的小案子，却在法庭上落荒而逃。最后他在孟买的律师生涯彻底失败，被迫回到故乡。此后，又发生了被甘地认为是带来他人生转折的事件，使他感受羞辱难当。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;甘地的哥哥虽然没有成为小邦国总理，却一度在家乡地位显赫。邦国虽小，印人自治的上层却是一潭混水。此时邦国已经有了英国人的政治联络官。甘地的哥哥被人告到联络官那里，被指涉嫌误导政治上层。他得知弟弟甘地在英国和此官员相识，就要甘地去说情。甘地明知不妥还是去了。官员很秉公办事的样子，说你不至于要利用我们这点友情来徇私吧。这名官员对甘地哥哥的印象完全负面，认为他是个政治阴谋家，也就特别不能容忍甘地的说情。他阻止甘地继续说下去，请他离开。甘地坚持不肯走并且继续往下说；该官员一怒之下就令仆人把他推了出去。甘地生性敏感，感觉自己受了奇耻大辱。他立即写信表示要告对方。对方回信叙述事件经过，表示自己没有做错什么，你尽管去告。关键是从法律角度看，也确实如此。对自尊心极强的甘地来说，这真是很糟糕的心理处境。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;甘地冷静下来后细看家乡，不仅政治纠葛复杂，而且，他要在当地开业，就难免要和那位官员抬头不见低头见。幸而甘地家族属于当地政治上层，社会关系和机会也多。此时一个在南非的印度商人有一个财务纠纷，需律师帮忙。甘地因哥哥的介绍得到了这个工作机会，就这样出走南非。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;到南非一段时间之后，发生了他被赶下火车的事件。乘警因甘地是个有色人种，不顾他拥有一等车厢车票，令他离开车厢。甘地拒绝后被拖下火车，行李也被扔下了车。历史学家们把这一事件看作“甘地开端”，是很有道理的。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;火车事件对于甘地，是在精神上绝地反击的开端。不久又发生他在坐马车时遇到车夫的歧视，他拼命反抗成功。甘地曾经形容自己是一个失败的学生、二流的律师。在此之前，需要他去征服的对象，不论是学业还是事业，似乎一直过于强大。作为三代邦国总理后代，敏感的甘地始终胸怀大志，却又在现实面前自卑。人和人不同，每个人合适的领域也各不相同，对甘地来说，需要丰富学识知识和分析推理才能实践的事业，并不是他的长处；坚持信念和追求真理才是他的特长。导致他离开印度的那次羞辱事件，几乎是青年甘地失败的象征。可是这一次，他的对手貌似强大却并不占理。看上去是对手打上门来，而在甘地内心深处，也许他自己都没有意识到，他准备这样一场精神翻身仗，已经很久很久了。他会不惜一切地以他的生命去抗争。他小小的身躯突然开始迸发出无尽的热情和能量。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;从自传可以看到，甘地拿着英国律师执照，却不是一个具备西方律师气质的人。他更象个僧侣或者说信徒。他没有皈依某个宗教，印度教显然是他的精神基础。看甘地的讲话和文章，常有长篇大论的感性表达，相反，理性推理、逻辑叙述的部分却弱得多。他注重精神探求，对于素食、禁食、禁医药、禁欲和其他禁忌等等，有着非常专注的思考，常常反复推敲体验、不断检讨自省。在妇女地位极低、童婚制的印度，甘地夫人成了他的绝对顺从者。甘地正当壮年时决定禁欲，他对夫人的态度是 “禁你没商量”。妻子对他的决定大多言听计从，其中包括在病危时不顾医生警告离开医院、禁医药和营养等等。甘地对学校有自己的看法，孩子想上学也不行。而这些决定的背后，都有在甘地看来很深奥的宗教思考和精神在后面。他是在履行信念，追求真理。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;这些家庭中的问题看上去是“小事”，却也折射了一个耐人寻味的问题。就是个人的精神探求、宗教信仰，应当局限在个人范围。若强加于人、或者说以某种强势向社会扩展推行，终有一些隐患在其中。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;有件事情可以看出甘地思路和现代律师有所不同。南非的德兰斯瓦在1885年通过一条法律，其中有这样的条款：有色人种和印度人都不能在公共人行道上行走。这条规定其实不仅是一个种族歧视的问题，还有劳工歧视。甘地最感到不平的，是印度人算在有色人种之列，而阿拉伯人却不算。究其原因，还是大多印度人在南非是被称为“苦力”的契约劳工。去年在印度火车站，印度人呼唤那些替旅客头顶手提五六个大箱子上旱桥、赚几个小钱的搬运工，就还是叫“苦力、苦力”。这条法律更是对“苦力”的歧视。因此，身为律师的甘地，常常不感觉自己也在被禁之列，总是走人行道。可是有一天，甘地被一个官员的门警从人行道上推开，打到街上。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;甘地的白人朋友正好经过，当场表示，自己作为目击者愿意为他到法庭上作证。甘地的回答是，“我已经决心不再为自己个人的疾苦打官司。”他的意思是，需要解决的是社会根本问题，他关注的社会不公正如果解决，假如社会问题解决，自己的问题也就随之解决了。这当然是一种有道理的角度，可也很典型地折射了甘地的思维方式。西方法律工作者的本能反应是，每个人当首先从维护自己的权益开始，自己受到不公正待遇，自然马上诉诸法律、以司法挑战的方式抗争；保护自己的权益就是为社会公平奋斗。而甘地的态度不仅更偏向一个社会改革家的角度，甚至隐含着某种东方式思维。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;一个带着热烈宗教情感的社会改革者，甘地产生以“非暴力”的方式进行政治斗争，是一件非常自然的事情。带领南非的印度社区以非暴力方式抗议歧视，是甘地找到自我位置、找到自信的起点。他在南非二十二年，虽然达到的目标是有限的，一些歧视法律被取消，可是新的歧视立法还在产生，可是，这毕竟是印度侨民的第一次胜利。消息传到印度，甘地成了众所周知的英雄人物。南非的成功给了甘地巨大的鼓舞。1914年回国的时候，他已经准备在一个更广阔的天地里推行他的非暴力运动。不知道他当时有没有为印度捏把汗，毕竟整个南非只有十万印度侨民，抗争的标的也小；而印度有两亿多人口，其目标和在南非的诉求也已经不可同日而语，他要的是印度独立。<br />
<br />
“非暴力之父”――重读《甘地自传》之二<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;非暴力，是能够历史悠久地坚持下来的大多宗教的核心内容之一。不论是哪个宗教，不论它们的神灵为何，对经典的解释都是由人在做，对非暴力的理解也是其中之一。非暴力概念本身的发生发展非常有意思，因为非暴力由“施暴”和“受暴后是否暴力反抗”这两方面组成。人类文明发展到一定程度，开始对残酷敏感、不愿意施暴，这和文明引发人的本性中善良的一面契合。而面对他人施暴，自己能不暴力对抗，不以暴易暴，宁死不反抗，却已经有违人的本能本性、必须仰仗非常强烈的宗教信念去坚持了。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;个人以被动承受暴力的非暴力坚持，再转化为以展示坚忍主动挑战暴力，不惜承受悲惨后果。这样个人自发的所谓非暴力抗争，在宗教冲突刚刚开始的一刻，就已经在发生。记得读法国人让・德科拉写的《西班牙史》，里面就提到在3世纪初，罗马人的宗教迫害启动了基督教的“殉教时期”，有大批教徒，甚至女童，主动挑战罗马权威，甘受酷刑处死。以身试法的非暴力抗争，即殉教，是宗教的一种极端行为。在初期，基督教教会对如此殉教显然是完全正面评价的。正因为如此，这样几近狂热的宗教热情会在民众中迅速传染，“殉教”才会一波接一波地高涨而形成“时期”。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;宗教信念都是个人化的体验。所以，当时即便殉教人数众多，也是分散的个体事件。700年后，在10世纪的西班牙南方，基督教再次受到伊斯兰教的压制，又发生新一波非暴力挑战的殉教浪潮，使得双方民众情绪激昂达到颠峰状态。这一次，基督教的主教们终于认识到非暴力挑战隐含了民众情绪失控的负面隐患，那些牺牲是不必要、不值得鼓励的。于是出来谴责挑战行为。替代的解决方案是两个宗教的高层政治交涉以达成妥协。历史在作出指点，哪怕是宗教性的社会改革，面对非公正权威，还是可能有两种不同的思路和处理方式。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;这么说来，甘地的非暴力似乎并不能算是一个发明。可是，他被称作“非暴力之父”是当之无愧的。“信仰”本身是宗教，而“争取信仰自由”却是政治概念。群体以非暴力抗争达到某个政治目标，就突然转变成了一种政治手段。甘地把历史上自发的宗教非暴力抗争，不仅发展成大规模发动民众的现代政治手段，也发展出相应的一套理论，最后也确实成为达到印度独立目标的重要原因之一。因此在历史上传为“佳话”。不仅如此，此后通过非暴力方式达到正义目标的历史事件，也都记在甘地那里，非暴力就变成了一个不必探究、绝对正确的神圣符号。可是，事实上，当一个基于个人信仰的个人行为，转化为有人发动有组织的群体运动时，一种质变就在发生，它变得错综复杂、需要具体问题具体分析了。<br />
<br />
甘地的非暴力言论很多，象《Ganhdi on Non－violence》这样的书也不少。这些言论大多是类宗教的信条。例如：“非暴力抵抗精神的获得，是一种在自我否定和欣赏我们自身内部潜力的长期训练，它改变一个人的人生观……它是最伟大的力量，因为它是灵魂的最高表现。”“我的一贯经验使我确信，除了真理以外，没有别的神。”“非暴力的两个基本点是：1．非暴力是最高法或宇宙法。2．除了真理没有任何别的法。”而“非暴力”就是他追求的占据神位的“真理”。甘地把政治性非暴力的理论，建立在类殉教的信念和热情上。这也是甘地经常宣布绝食的原因；从小母亲宗教性的禁食始终留在他心里。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;甘地的非暴力理论要求一个人在遭遇暴力的时候，把热情升温转化到几近狂热献身的状态，却不转为暴力。这连甘地本人都很难做到。他曾经写道：“没有自我纯洁，要遵行非暴力的法则也必然是一种梦想……然而自我纯洁的路程是艰难而崎岖的。一个人要达到完全的纯洁，就必须绝对摆脱思想、辩论和行动中的感情；超越于爱、憎、迎、拒的逆流之上。我知道我自己还没有达到这三方面的纯洁，虽然我在这方面一直进行着不倦的努力。”所以，非暴力引入政治号召，它的潜在问题是明摆在那里的：这就像做一个很难控制的易爆化学实验一样。你怎么可能使得情绪被煽动、相互感染的庞大非理性群体都如同一个个人那样，“纯洁”　到位，在强烈的刺激下保持恰如其分的分寸，而不是突然被激怒：“老子不干了”而转为暴力？它当然和人的整体素质有关，和社会环境有关，和对方的反应有关。甘地说： “经验教导了我，文明是非暴力反抗中最困难的部分，这里所说的文明并不是指在这种场合讲话要斯斯文文，而是指对于敌人也有一种内在的善意的胸怀。这应该在非暴力反抗者的每一个行动中表现出来。 ”当时的印度，恰是一个没有西方理性传统的国家，而大众整体教育水平极低。就是今天还有一半印度人不会读或者不会写。甘地说的是近一百年前的印度。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;“ 非暴力”如此深入人心，是大家基于字面理解，以为它绝对“没有”暴力。其实事情不可能那么简单。作为政治手段的非暴力、不服从运动，假如简化理解，不顾外部客观条件、不分青红皂白盲目推行，推出暴力推出悲剧来是很正常的事情。虽然甘地一再指出：“我坚决认为，带领人民进行非暴力反抗运动的领导人，应当能够把人民保持在所希望于他们的非暴力界限以内。”这也是典型的“甘地”特点，言论语录大多是无暇可击的“真理”，可是，与当时的现实很可能是脱节的。在甘地领导的非暴力运动中，也一样引发暴力连连。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;更何况，甘地的非暴力并不是“对方主动施暴，此方不暴力反抗”的所谓“消极非暴力 ”，而是要积极挑起对方的暴力。他认为：“在我看来，没有一种直接的积极的行动，非暴力就是无意义的。”“我想使弱者的非暴力变为勇者的非暴力，这可能是一个梦想，但我必须努力使之实现。”“不是通过把苦难强加给抵抗改革的人，而是通过自己承受苦难而达到改革的目的。因此，我们希望，在这个行动中，通过我们所受的深重苦难，可以影响政府”。“苦难”到什么程度呢？“把生命奉献给自己认为是正当的事情，是非暴力反抗的核心。”“一个非暴力反抗者决不可能逃避危险，不管他是在许多同伴中还是独自一人，只要他是战斗而死的，他就是充分履行了他的职责。”“勇敢在于赴死，而不在于杀戮。”“正像一个人在暴力的训练中要学习杀戮的艺术一样，一个人在非暴力的训练中也必须学习死亡的艺术。”这让我想起基督教的“殉教时期”。而那时，殉教也还不是教会组织的群众运动。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;很典型的是那次著名的盐税抗争。事先规划，发动2500名志愿者以非暴力的方式、但是违法强行进入盐库取盐。半年多前，我在印度大街上看到的印度警察还是当时的传统装备，就是一人一根粗竹棍。我亲眼看见一个牛车上的印度人，不知犯了哪一条，被交通警上去就是一棍，街上车水牛龙，没有人抗议。我目瞪口呆，路人熟视无睹。可见在1930年5月21日那天，盐库的几百个印度警卫会如何应对“抢犯”。面对数量十倍于自己、却一排排冷静上来的民众，警察竹棍腿脚齐下。这是甘地的非暴力追随者精心训练挑选的的敢死队，他们一排排上去，挨打、头破血流不还手，但是坚持要违法闯入盐库，直到倒地不起。他们实践甘地的非暴力精神“勇敢赴死”。血腥结果是事先料到的。后面的医疗队早就准备好包扎绷带，等候在那里。也事先通知了外国记者，这是“苦难深重”给政府看，也是给世界看，转而又是对英国政府的压力。这一非暴力行动的结果，警卫打伤320人，死亡两人。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;更多的情况是在非暴力运动中情绪激化或矛盾激化，造成双方都有失控，之后形成恶性循环。例如，1921年的非暴力运动中，在Chauri Chaura，游行民众与当地印度警察发生冲突，混乱中警察开枪，大多历史书称并未有民众被击中死亡，在一本书中指这是朝天开枪。我看到也有一本书说是有人被击中死亡。总之激怒的民众把20名警察和一名更夫锁入警察局，放火后全部烧死。最后甘地被迫中止这次的“非暴力”运动。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;在暴力发生在警察一方时，世界舆论的反应当然是强烈谴责暴力。可是回顾历史，是否也可以引出一个问题：这样的“非暴力”挑起可以预见必定要发生的“　暴力 ”（不可预见的不算），是不是解决问题的唯一方式？盐税不合理，在政府税收中的比例却很低。是否可能通过另外的方式，例如“议会道路”、修正立法来解决。印度的自治推进幅度相当大，1935年英国人制定的《印度政府法》，使得参加印度普选的选民已经达到3500万，其中包括600万妇女和300万“不可接触者”，国大党通过选举获得的议会议席高达70％。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;这另一条道路，从制度本身切入，逐渐改善民生，进行政治改革社会改善。很早就由于殖民带来的西风东渐而在印度深入开展。印度一直有自己的政治精英参政，在英国统治的区域争取扩大自治权。本土精英们很早就从立法切入，渐进改革。在甘地只有3岁的1872年，梵社领头，就促使政府颁布近代化的《婚姻法》，禁止童婚、禁止一夫多妻、容许寡妇再婚以及种姓之间可以合法通婚。顺便说说，虽然甘地不赞成对种姓之外“不可接触者”的歧视，却并不反对不同种姓不可通婚的制度。而直至今日我看到的印度，种姓制度仍然是非常普遍的事情。这不仅说明了印度的社会文化粘稠度－－从立法到推行极为困难，更说明了这个社会当时要进入现代社会，建立法治和民众的法治观念，是重要的事情。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;所谓先发社会的强国对后发社会的弱国经商入侵、甚至建立殖民地，从现代角度去看，无疑是非正义的。从历史角度去看，虽“错”却也是一个历史必然。欧洲的东印度公司进去的时候，那里还是个古代社会，印度次大陆各类征服者你来我往，不是什么稀奇事情。同样，随着人类进步，民族意识觉醒和现代国家意识确立，殖民地逐渐要求民族独立，强国或被赶出去，或者和平退出，或从殖民而转为联邦关系，也同样是历史的必然。可是，民族觉醒和殖民后退之间，必然有一个时间差，二者是不同步的。前者以什么方式推进，在暴力甚至战争，以及甘地的非暴力运动之外，也还存在一些其他道路。<br />
