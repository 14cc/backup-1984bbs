													<h2>万延海的几大硬伤</h2>
						
						
																											<div id="postmessage_799380" class="t_msgfont">　　第一、万先生在谴责共产党对维吾尔人的镇压和压迫的时候，使用针对性和谴责性的语言。而对于维吾尔人的暴行，则予以解释，并拒绝直接谴责暴徒和“二少一宽”政策，而代之以“让我们为所有在7月5日事件中失去生命的人民祈祷和默哀！让我们停止相互的攻击和仇杀吧！让我们互相帮助！让我们看到人权公约、宪法和法律的精神在中国和新疆那个地区得到实现吧”这种泛泛的言论。明显的双重标准。<br />
<br />
　　第二、如果维吾尔人在内地，如万先生所言，不能获得“基本的人类需求”，那么他们可以离开内地。如果他们没有路费或者处于被劫持的状态，万先生应该呼吁政府和社会各界，严厉打击劫持者，并给他们捐献路费。这是解决问题最基本的思路。但对此种可能，万先生甚至从未提及。<br />
<br />
　　第三、万先生提出“我们汉人统治维吾尔人”的谬论，在被指责以后，又以“我们人类的智慧是有局限的。在普通维吾尔人看来，统治者可能首先是汉人，而不是共产党人。在汉人看来，统治者是共产党，不代表汉人。”进行辩解。<br />
　　注意：这种辩解是空洞的，因为他还是不承认“我们汉人统治维吾尔人”是谬论，而以“我们人类的智慧是有局限的。”加以掩饰。更可笑的是：他本人应该不是“普通维吾尔人”，为什么要按照普通维吾尔人的误解来说话呢？<br />
<br />
　　第四、万先生说：“真正依法办事，维吾尔人应该欢迎的。海莱特先生在文章就积极呼吁依法办事。我认为缺少同情心的话，依法办事没有效果，这和民族关系无关。我们的用词用语要小心。我们是打击小偷，而不是“新疆小偷”；我们打击暴力恐怖主义，而不是维吾尔人民任何自发的团体。”<br />
　　也就是说：是否依法办事是否有利，要看你是否同情心，你不同情维族小偷，那么依法办事也没效果。这样的荒谬逻辑，是第几世纪的水平？<br />
　　而且，现在的事实是：二少一宽政策阻碍了执法机关象打击汉族小偷那样打击维族小偷。在这种情况下，不许提及小偷的具体身份，又怎么能够反对二少一宽政策呢？<br />
<br />
　　第五、万先生说：“狂热的含民族主义情绪，会伤害中国人的。一方面，我们可能失去理智，一方面我们激发更多的敌人。汉人的民族主义估计也会激发各个民族的民族主义情绪的。我们的科学兴国理念和和谐社会理念需要坚持。需要务实。”<br />
　　试问：75事件专门以平民为屠杀对象，还有比这更严重的民族仇恨吗？连911，都是对着标志性建筑去的，而不是专门为了杀人。维族展示了如此极端的民族情绪，到万先生嘴里，倒成了“汉人的民族主义估计也会激发各个民族的民族主义情绪的。”<br />
<br />
　　就写这些吧。欢迎就事论事地进行批驳，不欢迎离开上述内容，瞎扯别的东西泄愤。<br />
