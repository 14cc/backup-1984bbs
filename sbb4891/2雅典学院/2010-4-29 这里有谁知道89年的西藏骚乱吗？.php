中共中央电令镇压藏人游行<br />
<br />
　　三月四日下午，中共中央办公厅，军委办公厅联合向自治区党委军区发布了由赵紫阳、杨尚昆共同签署的电报指示；大致内容如下：<br />
<br />
　　（1）武警作好全面作战准备，密切注视局势的发展，随时报告局势的详细动态。<br />
<br />
　　（2）公安系统配合武警，立即严密封锁拉萨与外界的消息。对正在拉萨地区旅游的外国人，准备集中撤出。在边境口岸和机场需严格检查行李物品，防止动乱消息外漏，造成国际影响；必要时，可违反惯例和规定强行检查。<br />
<br />
　　（3）对各大寺要严密监视，必要时，可先行逮捕已在危险人物档案中挂名的反动宗教人士；不要怕抓错，要坚决防止全藏区宗教界的串联。<br />
<br />
　　（4）边境部队进入一级战备，严密注视印度方面的动向，随时向中央军委及总参报告边境动态。<br />
<br />
　　（5）对内地进藏工作的新闻人员，讲明不得采访动乱情况，有违者可就地处置。<br />
<br />
　　（6）对尼泊尔、不丹、锡金各商业机构、各领事机构加强监视。如发现问题不得擅自行事，急报国家安全部待批后再统一行动。<br />
<br />
　　（7）立即封锁拉萨市郊各条公路，立即封锁当雄、贡嘎机场。任何人不得擅自接近军事设施，违者就地处决。<br />
<br />
　　（8）对国家安全部报来的外国新闻机构住藏人员，逐一查清，不得漏网。查到后，待安全部执行小组到达时移交。<br />
<br />
　　（9）自治区党委及自治区政府应尽快拉萨各党政军机关人员做好一切应变准备，对擅离职守者严加查办，不得姑息。<br />
<br />
　　（10）上述各条要求于三月六日前准备就绪。中央各直属部门将进行具体工作指示。<br />
<br />
　　当自治区党委第一书记胡锦涛宣布了上述十条电报指示后，在座的各位都不啃声了。其实也没有必要再啃声了，结论是清楚的――要和这里的藏人为敌了！<br />
<br />
　　刚才他们中的大部份人还站在藏人的利益和感情上辩论、争吵，然而有了这样一个命令之后，他们是会马上转变态度的。斯大林曾经说过：“共产党人是用特殊材料制成的。”这句话是有道理的。<br />
<br />
　　就在这天上午，拉萨的人们已经抢先一步走上了街头，大街小巷里藏人们随着一些僧尼高呼着口号，向人民广场走去。队伍显得有些散乱，人们说着，笑着，显出漫不经心的样子。象一年一度的雪顿节，很多人还提着装有甜茶的暖瓶。街上很少看到汉人，甚至根本没有警察。在藏人的眼睛里，大概这就算是一种宗教的胜利吧。有人在广场上高唱起了“雪城的理想”这首歌，在场的人们都跟着唱起来。这首歌听起来象是在祈祷。一些僧尼开始咏经，声调委婉而低沉。一时间广场上显得肃穆而庄严。<br />
<br />
　　本来这时应该召开一年一度的藏历传召大法会，但这年的法会没有召开。据宗教界的人士说：达赖活佛不能回来，班禅活佛又圆寂了，传召失去了意义。但政府内的有关人士说：中共西藏当局已告知宗教界今年的法会停止，原因是近年来每次法会都成为反中央反共的聚会，政府不能允许这样的法会再开下去。什么时候再开要看西藏宗教界的态度而定。――也许就是为此，拉萨的人们才这么热衷上街的。<br />
<br />
　　其实只要是能发泄心中对这个政权的不满，开不开法会已经不是什么重要的事情了。今天人们前往大昭寺前的人民广场，是为了法会，但也不是为了法会。<br />
<br />
　　下午四时，人们高呼起反政府的口号，人群出现了空前的激昂状态。五时左右，一队武警和一队公安警从老拉萨饭店处开来。顿时引起了人群的骚动，一些僧尼站在人群的最前面，直视着开来的队伍。当队伍走近广场时，一名军官模样的武警走上前来向人们解释说：他们不是来干涉大家的，是要到八角街里执行任务。并请人们让开。然而人们并没有为他们让开道路，而是更加紧密地站在了一起，并依然高声呼喊着口号。一些藏族年轻人把警察的队伍围了起来，大声地斥责他们，并高喊着要他们滚蛋。武警和警察的队伍被围在人群中间，一时不知如何是好。正当一些藏族青年挥动着手中的家伙要击上去的时候，人群中一位年长的喇嘛高声制止了这群年轻人。他走上前去，双手合十，然后对军官说：“你们的岗位不在这里，这里是我们居住的地方。请你们走吧。要不然激怒了他们，你们是要吃亏的，走吧！”然后他向身旁的年轻人们用藏语严肃地告诫着，那意思是：他们这些兵不过是听了上面的命令来的，他们没罪，不要为难他们。这位年老僧人的话很起作用，年轻的藏人收敛了刚才的情绪，人们在这些武警队伍的身后让开了一条路。这时那位军官握着老人的手说：“谢谢您，老大爷，我们理解您的意思，我们就撤。我们汉藏人民没有矛盾。”说罢，命令队伍向后转，走出了广场，沿原路返回了。望着远去的警察队伍，人群欢呼雀跃，很多藏人竟激动地流下了眼泪。这是一场力量的较量，在这场较量中，藏人们看到了胜利的可能――那位年长的僧人站在大昭寺的高台上，望着远去的警察队伍，心情沉重地咏吟了一句萨迦格言：<br />
<br />
　　恶人有时也变得温顺，<br />
　　但那却是伪装的行动。<br />
　　玻璃涂上了宝石的光泽，<br />
　　遇见水就会暴露出本色。<br />
<br />
　　第二天，这首格言贴满了拉萨的大街小巷。<br />
<br />
