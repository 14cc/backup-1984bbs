　　西藏这个民族是神秘的，关于这个民族的起源至今对于人类学家来说还是个谜。这个民族曾经是非常强大的，他们的疆域曾西起克什米儿，东到甘肃、青海，南至四川、云南、大理，北抵今天的吉尔吉斯和蒙古。因为唐朝的和亲和印度与中国的佛教和平进攻才使这种凶猛的扩张嘎然中止。<br />
<br />
　　近一千年来，他们介乎于两大文明之间，时常因为内部的教派纷争而转向求助于东方的那个强大的中华帝国。在印度和中国这两扇文化磨盘的挤压中，这个民族的政治变得既圆滑又坚硬。这个民族对自己的宗教是绝对崇拜的。在五百一十九尊神面前，他们始终觉得有为自己的信念而献身的必要――自从元朝忽必烈在这里开府，沿明清一直到民国这段漫长的历史中，哪个中国政府都没能真正的占领过这块坚硬的高原。<br />
<br />
　　然而，四十年前，终于有一股强大的力量碾碎了这颗圆滑而坚韧的核桃。中国共产党的军队占领了这里，并且一直统治到今天。他们高唱着解放全人类的圣歌，推翻了西藏的政府。在民主改革和文化大革命的喧闹声中，他们摧毁了百分之八十的寺庙，驱散了百分之九十的僧众。在红旗插上布达拉宫时候，西藏的宗教领袖达赖被迫出走印度。在毛泽东挥舞军帽宣告文化大革命开始的时候，另一位宗教领袖班禅锒当入狱达十年之久。――他们把一个新的宗教硬塞给这个神秘的民族，他们改造了这个社会，也妄图改造这个民族的品格。在这个新的社会里，他们滋养着强权，残忍，逆来顺受，苟且偷生；尊严与地位成比例；才能要听命于权力；独裁居然写进宪法，罪人也能万古千秋；嫉妒铲平人们的高矮，虚荣埋葬了人们的心智；有口无心是生活的主食，得过且过的将就是每天的家常菜；傻乎乎的激情是有志向，自欺欺人的愚忠是有气节；心里想的什么不能讲出来，嘴里讲出来的尽是废话――当他们宣布发现了一个真理的同时，他们也埋葬了一个民族古老而质朴的灵魂。<br />
<br />
　　现在，这颗灵魂在埋葬了四十年之后，开始发芽了。<br />
<br />
　　三月三日至四日这一期间，当党政军会议上呵欠连天的时候，拉萨的人们却异常活跃。人们在街头巷尾，在甜茶馆里三五成群，交头接耳，互相传递着反判的信息。一股无形的力量在聚合，在窥测，在策划着对一个强大政权的撞击。<br />
<br />
　　这两天，拉萨市中心的八角街和人民广场看不到往日行色匆匆的内地商人，转经道两旁的货摊空了许多。这里已经是藏人的天下，只有少数国内外的游客行至其间，他们绕开当街堆起的障碍物向前行进的时候，感到了一种莫名其妙的恐惧。<br />
<br />
　　康巴的商人们坐在支起的本田摩托车上，使劲地加大油门，干轰着马达，浓烈的油烟布满了深暗的街道。一些年轻的藏人在街上溜达着，手中持着一些说不出名堂的家伙，作出深呼吸状吸着汽油烟，脸上显出某种满足的神情。更多的妇女和老人们都在忙碌着往当街的障碍物上堆放砖头和石块，儿童们则坐在障碍物上尽情地玩耍。<br />
<br />
　　如果不知道近几天来拉萨发生的事情，初来者还以为是拉萨市政当局正开始准备改造城区建筑呢。<br />
<br />
　　似乎还有人往八角街赶来，当然也有一些人正从八角街匆匆离去。这一来一往使拉萨城开始出现了微震的征兆。<br />
