　　三月五日上午十时，武警在八角街的情报系统向临时指挥部报告：“有近五百名喇嘛和尼姑及一千多名藏民已做好闹事的准备，大多数都集中在大昭寺内，但什么时候开始还搞不清楚。目前各小组正严密监视其动向，有情况将随时报告……”<br />
<br />
　　整个上午，消息不断从八角街里传出，武警士兵们已呈临战的状态。这些兵们和藏人结了仇，在八七年和八八年两次大规模的冲突中，他们几乎是没有什么武器配备就被投入了那样的对抗中。他们手中的盾牌和木棍在藏人们的雨点般的石块攻击下根本无法发挥力量，因而受伤的人很多，平息反抗的效果也很差，中共及地方对此都很不满。这些四川兵满腹牢骚，抱怨中共没有给他们绝对镇压的权力。“拿我们的肉往他们的石头上撞，这些中央的龟儿子真想得出――下次再有事，如果不发枪发子弹，老子就扒了这身狗皮回家……”一些士兵时常这样愤愤地骂着。然而今天，上面不但给他们配备了非常有效的武器和充足的弹药，同时还给予了他们杀的权力。这些士兵知道：那些藏人没有他们这样的装备，而他们却可以任意地向这些藏人射杀，用强大的火力来平息那些藏人们近乎可笑的反抗。这些平均年龄只有二十几岁的年轻士兵，此时的兴奋状态是可想而知的，一股野性的怒火燃烧着他们。他们都很年轻，可能从生下来手上就没有沾过血。――也许，此时此刻这些年轻的士兵中有人思考过人性这个东西，但是来不及了。战争，这扇磨盘已经开始转动，一切人性的哲学思考都会被这扇大磨无情的碾碎，枪机将服从所有的食指，弹道将不再是一条正直的线。今天，人类一切原始的残忍，一切邪恶的欲望，都笼罩在“保卫祖国统一，维护安定团结”的烟雾之中。犯罪者只要能借任何一条公认的法理，就可以心安理得地去杀，去抢，去奸淫，这似乎是中国传统政治的特色。在这种政治特色的渲染下，很多清白正直的人走上了犯罪的道路，造成了一代又一代人终生的遗憾。<br />
<br />
　　三月五日中午十二时左右，武警在八角街的情报系统再次报告：“……由十三名喇嘛和尼姑组成的队伍正沿着转经道走向人民广场，八角街里很多人已经准备冲出去，监视还在继续……”正当武警临时指挥部接到这一报告的同时，一支由喇嘛、尼姑及部份藏人组成的队伍从转经道口走了出来。他们显得很从容，没有一点畏惧的神情。在人民广场上，这几十个人站住，由一名喇嘛打开了“雪山狮子旗”，慢慢地举了起来。旗帜在风中冽冽地飘舞，这面旗帜呈金黄色，在蓝天中映衬，似乎使人们想起了这个民族古老而悠久的光荣。<br />
<br />
　　居高临下的武警士兵们握紧冲锋枪，拉开枪拴的声音响成了一片。有些士兵伸头向下望去，只见从八角街里涌出了无数藏人。立刻，广场上沸腾起来，杂沓的脚步声激起了一片喧哗，旗帜开始移动，人们跟在旗帜后面，声斯力竭地呼喊起要求独立、反对中共政权的口号。当旗帜指向转经道口的那一瞬间，从东向西沿着转经道的示威便正式开始了。时间是一九八九年三月五日中午十二时十四分。<br />
<br />
　　藏人们高举着拳头和标语牌随着“雪山狮子旗”前进，当他们转到第一圈时，广场上已经挤满了围观的人群；他们转到第二圈时，参加者便堵塞了整个八角街，使八角街里水泄不通，这时人群已达一万人左右，并且还有人不断地向八角街和人民广场涌来。这时的八角街和人民广场已经没有任何可以站立的地方了。<br />
<br />
　　中午十二点四十分左右，人们开始冲击八角街派出所，大小不一的石块、砖头砸向派出所的门围，顷刻之间，派出所被愤怒的示威者摧毁了。很多人挤在派出所里，这些人中有些显得很老练，他们搜查派出所的每一个房间；然后撬开办公室里的所有的抽屉，开始很仔细地翻阅着档案柜里的纸张，行动异常敏捷。随后，他们将档案纸张装进随身带来的麻袋里，挤出了派出所，消失在人群之中，全部过程只有十分钟左右。一些藏人发现了这一情况，尾随着他们想看个究竟，但在八角街口处，被人围殴，有两名藏人被人用皮带活活勒死。没有人知道这些人是谁，也没有人知道他们拿着这些档案去了哪里。<br />
<br />
　　八角街派出所的档案里，记录着八角街所有居民的情况，尤其记录着一些特殊居民的情况。自一九八三年西藏对外开放以来，中央及地方很多部门为了更多地了解情况，掌握第一手资料，都纷纷涌进了这狭小的巷子里，在经商业务的掩护下，建立起了一个个的独立的情报系统：中共中央统战部、国家民族事务委员会、国务院宗教事务局、中央纪律检查委员会、中央财政部、中国税务总局、国家文物保护局、国家商品检验局、中国海关总署、成都军区情报处、兰州军区情报处、新疆军区情报处、西藏自治区军区情报处、中国公安部、安全部、总参谋部、中国商业部、供销合作总社等等，待大大小小的中央及地方机关都开始在这里安营扎寨。武警系统来得晚了一些，但他们由于手持上方宝剑，凭着中共特许的众多权力，竟在三年时间内完全控制了这个西藏最繁华的地区，成了首户。他们随意征集民房，开设商店，把自己的亲属安置进来，甚至招来温州和四川的姑娘在此开设地下妓院和赌场。他们对自治区各级党政机关说：他们这样做是为了工作的需要，因为他们负有保卫西藏地区社会治安的巨大责任。他们既然敢这么干，当然是有充份的理由和根据的。<br />
<br />
　　为此，很多单位愤愤不平，在报回去的材料中每每都表示了对这个外来户的不满。渐渐地，各种力量在各自既得利益的基础上，在西藏拉萨这条街里，对武警系统结成了一个神圣同盟。<br />
<br />
　　自一九八七年到一九八九年，各系统、各单位用同一个调子、同一个嗓门向中共中央有关单位呼吁，要求解决武警在西藏特别是在拉萨的问题。甚至在全国人大常委会上，班禅也站到了他们一边。据中共党内消息灵通人士讲：一九八七年九月以来，武警利用对两次反抗活动镇压的机会，大肆逮捕了一些藏人，这些藏人都是各系统和各部门的情报人员。武警在拉萨地区及其其他地区的丑闻就是这些藏人透露给各系统的。然而矛盾并没有随着武警对各系统眼线的清洗而趋于缓和，反而导致了这个同盟更加猛烈的攻击。在西藏全境，武警势力似乎受到了空前的孤立。在一九八九年三月这一期间，随着藏人反抗运动的进一步发展和扩大，中共内部的矛盾也上升到了白热化的程度。从政治坐标的曲线来分析，这两种截然不同的冲击是有着某种内在的联系的。<br />
<br />
　　在西藏，人们把这种对抗称为公开的“秘密战”。在北京，中共上层的消息灵通人士则把拉萨的八角街喻为西藏的“卡萨布兰卡”。<br />
<br />
<br />
屠杀<br />
<br />
　　五日中午一点四十分，武警部队开始向人民广场发射催泪弹，人群立刻大乱。催泪弹爆炸后放出的浓烈气味，把人们逼进了八角街转经道的几条小巷里，广场上只剩下少数不愿离去的年轻藏人。他们用毛巾或衣服堵住鼻子，蹲在广场边缘一些建筑物的角落，继续高喊着口号。<br />
<br />
　　两点十分左右，大昭寺正面的高大“塔经”被人点着了，熊熊的烈火直冲天空。藏人们看到这种情况，纷纷涌上前去灭火。“塔经”是藏传佛教中密教的一种形式，它是一种在高大木椿上裹满写着佛教藏经的五彩布条构成的建筑。一般这种建筑都矗立在寺庙和各种宗教场所中。“塔经”象征着保佑众生避恶驱邪、吉祥幸福的佛法佛力。在藏人眼里，它是至高无上的佛袒法杖，它显示了密教众神在人世间的威严。面对燃烧着的“塔经”，藏人们无不痛心疾首，很多老人和妇女都失声痛哭起来，年轻的藏人们则愤怒地高声叫骂着。“塔经”显然被浇上了大量的煤油和汽油，高温使人们一时无法接近。<br />
<br />
　　正当一部分藏人提着工具准备灭火的时候，武警士兵从人民广场两侧的高层建筑上向“塔经”周围的群众开火。藏人见开了枪，都纷纷抛掉手中的水桶和灭火工具趴在地上，然后猫着腰迅速地跑回八角街里。有七八个藏人被打倒了，受伤的藏人忍着伤痛大声呼喊着，希望街里的人能赶来救护，但密集的枪弹把八角街转经道口封得死死的，没人能冲出去救回那些受伤者。街里的人们眼睁睁地看着躺在地上的受伤者和正在熊熊燃烧的“塔经”，很多人双手合十，念经祈福，希望这些人还能有幸生还。<br />
<br />
　　两点半左右，高高的“塔经”在浓烟烈火中倒塌下来，横垣在大昭寺前的空地上继续燃烧着。就在这时，几十颗催泪弹有一次射进八角街里，浓烈的白烟弥漫了人们的视线，刺鼻的气味呛得人们大声地咳嗽着退向小巷的深处。随着一阵警笛的呼啸声，来了三辆消防车在一百多名全副武装的武警战士护卫下使进人民广场，他们很迅速地扑灭了正在燃烧的“塔经”。在灭火的时候，武警部队的政工人员手持摄像机拍下了救火的场面。一片硝烟退去之后，广场上除了正在冒烟的“塔经”之外，什么都不存在了。即没有士兵和消防车，也没有那些被打伤的藏人们。当几个胆大的藏人想走出街口上前去看个究竟的时候，被从广场两侧射来的枪弹又一次地逼进了小巷里。显然，八角街通向人民广场的一侧被严密封锁了，任何人都不可能冲出来。<br />
<br />
　　下午三点左右，武警特别分队开始合围位于市中心的八角街，他们用催泪弹射向人群，迫使他们退入八角街的几条狭窄的巷子。藏人们利用八角街中搭起的障碍物做为掩护，和前来的武警部队展开了激烈的对抗。由于藏人们顽强的抵抗，武警占领八角街的企图未能如愿。正当四百多名手持盾牌和冲锋枪的武警从八角街里退出来的时候，青年路、北京东路一带也开始了激烈的对抗。三百多名武警士兵被藏人的砖头、石块儿封锁，无法对市中心地带构成钳形包围。藏人们用“骨剁”（一种赶羊用的投石器）对这三百多名士兵进行了集中进攻。这种投石器是藏人们得心应手的武器，用这种投石器藏人们可对远距离的目标进行准确打击。很多正在冲击的武警士兵纷纷被从百米之外投来的石头打倒，攻击受阻。武警士兵只得退守到房屋角下，凭借着建筑物以躲避砖头石块儿的攻击。<br />
<br />
　　下午三点十分左右，武警临时指挥部召开了一个联席会议，会议决定：<br />
<br />
　　“鉴于目前正在拉萨旅游的外国人还没有全部撤出，所以原定的平暴方案暂缓执行，各分队要坚决防守住各重要机关单位和公共设施，不到万不得以不要开枪射击。如有被打死打伤的暴徒和市民，应立即收押，不得被其他暴徒收走，以免造成不必要的影响。对城关区内的其他设施，可暂时放弃防守，集中力量保证各点的军事部署不受影响。对接近重要保护单位和设施的暴徒，要坚决围歼，在围歼中要作到干净、彻底，尽量不使暴徒漏网，以免造成其他连锁反应。”<br />
<br />
　　下午三点至五点，武警部队奉命分别弃守了市中心的城关区政府、市公安局城关分局、吉日派出所和居民委员会、拉萨市小学校和城关区粮站。藏人们在占领这些地方时，几乎没有遇到任何阻挡。城关区政府的牌子被藏人们砸毁，市公安局城关分局林街的一面的窗户全部被藏人们砸烂。由于武警部队在人民广场一侧的严密防守，藏人的冲击自治区政府和军区大院的设想已不可能施行。于是，他们从八角街的北面和东面涌出去，在北京路、林廓路、青年路一带设置了对武警的防线。由于武警方面对市中心的弃守，这时的拉萨市中心地区基本为藏人们占领。一时间，市区内大乱。<br />
<br />
　　内地来拉萨经商的人害怕藏人们的报复，纷纷放弃了自己的饭店和商店，向城外逃避；但是绝大多数藏人们不但没有伤害他们，反而帮助他们向城外疏散。在青年路、北京路一带，很多内地人在藏人的掩护下向城外转移。但当他们刚刚走出北京西路和青年路时，却遭到了来自武警防线一侧的攻击。催泪弹和石块儿雨点般地打了过来，使他们又不得不退回藏人的防线内。他们不明白武警部队为什么要攻击他们，藏人们也不明白。在林廓路，三十几名年轻藏人护送著一些内地人和藏族的老弱病残人员向外城突围。待他们刚刚冲过武警设置的警戒线时，就被密集的冲锋枪弹封锁住了。随后，有成百的武警包围过来，带走了这批人。防线内的藏人们看到了这一幕，他们用话筒向武警喊话，试图说明那些藏族青年是护送汉人们去城外避难的。但对方的回答是机枪和冲锋枪的点射及十几枚催泪弹。几乎没有任何对话的可能，也没有任何办法走出去了。藏人们面对现实，似乎预想到了形势的险恶。<br />
<br />
　　由于城区失去了供应，一部分藏人市民开始收集物资。城关区粮店被砸开了，正当人们蜂拥上前准备装这些粮食的时候，却有人往粮食里泼上了大量汽油、柴油，点燃了熊熊大火。在其他的地点，物资被藏人们从商店里抱出来堆放在街上准备分发给群众的时候，也被一一点燃烧毁了。汽车、救护车、大量的自行车等交通工具也同样的这样被处理掉了。这时的市区，到处火焰冲天。一些藏人猛然明白了这是武警便衣们干的，于是，他们见到汉人和内地人就打，他们在这种严酷的现实中开始绝望了。<br />
<br />
　　整整一天，两万藏人在市中心东突西撞，但他们再无法冲出去了。很多饥饿的外地藏人开始进入本市居民家中寻找吃喝的东西，致使市中心的居民出现了普遍的恐慌状态。<br />
<br />
　　三月五日六点左右，在武警和公安的严密监视下，几百名正在拉萨旅游的外国人被集中在拉萨西郊的假日旅馆，开始进行有步骤的疏散。一部分团体游客前往日喀则，个人旅游者则分两批分别经樟木口岸前往尼泊尔、不丹；另外一些在清查名单上挂名的有特殊身份的外国人则被送往贡噶机场，在那里接受检查，然后乘机前往成都和北京。当武警系统和拉萨市公安局外管部门于晚间八点清点人数时，发现还有三十七名外国人仍然留在拉萨地区，没有集中。他们立即将这一信息报告了武警临时指挥部。晚间九点左右，临时指挥部下令各单位，一定要在六日拂晓前将这三十七名外国人找到并且集中到一百公里外的贡噶机场待查。<br />
<br />
　　五日深夜，聚集在市中心的藏人分为几股向城外突围，但由于武警方面的严密封锁，只有少量的人突了出去。武警方面出动的小股部队在市中心地带也受到了藏人们的顽强抵抗。这一夜，拉萨市边缘地区和市中心地带形成了藏人和武警部队的僵持局面。武警企图以小股部队进入市区中心清查的计划，由于藏人们的顽强抵抗而无法执行。直到六日清晨七点，武警小股清查部队只在市中心找到了十二名外国游客，其他二十五名外国游客仍然不知去向。<br />
<br />
　　三月六日凌晨，武警临时指挥部向各参与镇压的单位发布了在拉萨开始公开进行镇压的命令，命令内容如下：<br />
<br />
　　一、各单位必须于六日八时全部按战斗序列到位。<br />
<br />
　　二、十时开始对八角街地区发动进攻。<br />
<br />
　　三、外层警戒部队作好战斗准备，对从市区逃出的暴徒进行收押，如遇暴徒抵抗，格杀勿论。<br />
<br />
　　四、中午十二点整开始进行市区全面清查，外层部队要保证警戒线的稳固，防止暴徒漏网。<br />
<br />
　　五、公安厅、局各单位，除协助武警部队进行市区清查外，还要保证城区道路畅通和安全消防工作。<br />
<br />
　　六、武警各单位在清查过程中要严格按照战斗序列中的规定进行。在清查过程中，要做到干净彻底，谨防事态扩大和战斗情况外漏。<br />
<br />
　　七、在清查和攻击行动中，各单位要和指挥中心保持联系，遇事要坚决请示，不得擅自处理；对所有收押的暴徒，统一押往拉萨八一农场，任何单位不得单独收押。<br />
<br />
　　八、清查行动定于七日下午五时结束，各单位务必加快工作进度，争取提前完成任务。<br />
<br />
　　三月六日早晨，武警部队特别分队开始强行进入市中心。他们从南、北、西三个方向同时进行，先后占领了城关区委和区政府、市公安局城关分局、八角街派出所和居民委员会、吉日派出所、吉日治安服务站、吉日小学、雪城旅馆、亚旅社、城关区粮站、单集村居民委员会等市内重要设施。与此同时，武警部队开始沿拉萨市郊设置警戒线，大批武警乘坐著军用卡车沿线布防，把拉萨市严密地包围起来。在通向郊区的各条公路上，都能看到成片的军车和荷枪实弹的武警士兵。<br />
<br />
　　三月六日九点，挂着公安局牌子的八辆广播车，在武警摩托车队的掩护下，开进市区。沿青年路、林廓路和北京路，用汉藏两种语言进行广播；要求所有居民不要上街，以免发生意外。中共西藏当局的这一举动，使聚集在市中心的藏人们清楚地认识到：局势正在发生重大的变化，尽管他们并不知道中共会用什么方式来平息这次反抗。藏人们有些慌了，在市区有家的藏人跑回了家里，将大门反扣起来，心绪不宁地向外张望着。那些从外地来的藏人们看到这种情况，也开始寻找退路，有些人试图躲进市民的家里，但大多数人都被本地居民拒绝了。在一种绝望心理的支配下，几千名外地藏人组织了起来，准备突围。<br />
<br />
　　一九八九年三月六日上午十时，中共驻藏武警部队会同中共西藏地方公安部队开始合围拉萨市中心。<br />
<br />
　　十点左右，在八角街通向人民广场的几条小巷里，据守在街垒后面的藏人们发现：武警分成小股正沿着小巷两旁的向街里靠近。藏人们开始用砖头、石块回击，在雨点般的石块儿砖头的攻击下，武警士兵们停止了向前的推进。街垒后面的藏人看到这种情况，误以为是武警力量有限，便开始冲出街垒向武警战士扑去，大约有三百多名年轻藏人以石块儿、砖头和木棍为武器，追击向后撤退的武警士兵们。当他们快冲到巷口时，大昭寺顶和转德道两旁的房屋上突然出现了大批武警。还没等到那些冲击着的藏人们明白过来是怎么回事，机枪和冲锋枪弹就普天盖地地向他们射来。在居高临下的武警士兵眼中，这些藏人成了活靶子。密集的枪弹从前后左右射向这群藏人，十分钟过后，这三百多人全部被打倒在转德道的当街。一些受伤的藏人哭叫着向街里趴去的时候，武警士兵向这些人补枪，将他们击毙。街里的藏人们看到这残酷的一幕，都吓呆了，随后乱了阵脚，尖叫着向四面的小巷里躲避。武警特别分队踩着死伤藏人的身体进入了八角街――屠杀正式开始了。<br />
<br />
　　在青年路一带，武警开始对据守在马路上的藏人进行围捕，藏人们一边抵抗一边向城区居民去撤退。在冲赛康市场通向北京东路的地方，一群藏人在两名藏族青年的率领下，以砖头、木棍和“骨剁”为武器，向拉萨大桥方向突围。当他们接近武警防线的时候，受到了来自正面火力的猛烈攻击，一些藏人被枪弹打倒，其他人则四散逃去。那两名领头突围的青年被十几名武警士兵追赶，他们逃进了靠近青年路的一户藏族居民家里，武警士兵也尾随着冲了进去。面对冲进来的士兵，两名青年当即举起了双手，表示屈服。但武警一名下级军官不由分说地抬起冲锋枪将两人扫倒，与此同时其他士兵也扣动扳机，对屋里的所有人进行扫射。两分钟后，两逃亡者和这户人家的九口人的身体都被打成了蜂窝状，全部被杀死。这户人家的三个孩子在武警冲进房间的时候，正坐在床上做功课，死时手中还握着铅笔。<br />
<br />
　　三月六日这一天，拉萨一片恐怖，到处都有人被杀，一时间叫喊声和痛哭声混成了一片。一些走投无路的藏人开始试图占领一些公共场所和设施。当他们接近这些地方时，遇到了守卫在那里的士兵的猛烈打击。成群的藏人在武警“缴枪不杀”的镇喝声中抱头卧在地上，被冲上来的士兵用绳子捆起来押上了卡车。其实，这些藏人手中除了棍棒、石块儿外，根本就没有像样的武器。在武警特别分队的残酷打击下，据守在市中心街垒后的藏人们开始屈服，不断有人举起捆着白布条的小棍，从街垒里走出来向士兵们投降。从六日上午九点到下午五点，这短短的时间里，武警方面就收押了近三千人。晚间七点左右，临时指挥部向中共中央报告：武警、公安系统已经成功地控制了拉萨全市，并准备深入进行清查行动。<br />
<br />
　　武警方面不择手段的镇压方式使西藏地方党、政、军感到震惊……<br />
<br />
　　在由自治区党委召集的联席会议上，自治区党委、人大常委会、政协、公安厅等方面对武警的这一作法公开表示反对。会议上，各单位负责人指责武警方面把问题搞糟了，指出这无疑是对今后的西藏社会安定埋下了一颗定时炸弹。政协方面甚至公开指责武警的这种过火行动是一种报复行为，是对党政各部门近年来对武警批评的一种情绪化处理。会议上，公安厅对武警的战斗序列计划提出了众多疑问，并声明自治区公安厅不能执行这种计划，他们将撤出所属系统的全部执勤人员……<br />
<br />
　　三月六日晚八点，自治区公安厅、拉萨市公安局系统约三千名警察，撤出了拉萨市区，拒绝与武警合作。<br />
<br />
　　三月六日晚十一点，自治区党委将这一情况紧急报告了中央统战部和中央办公厅，并要求中央拿出补救方案来。<br />
<br />
　　三月六日晚，驻林芝的国防军五十二旅，奉中央军委之名开赴拉萨。沿一四○号公路和九十二号公路，对拉萨地区的达孜、赛哈、德崩、堆龙德庆、贡噶、曼托林完成了外线布防。<br />
<br />
　　三月七日，由于自治区公安系统采取了不合作的态度，市区的武警部署出现了兵力不足而导致的防卫空虚。面对这种状况，武警方面希望国家安全部工作小组出面向自治区党、政、军、警各界讲清楚：武警的战斗序列计划是中共中央认可的，并不是本系统擅自作出的决定。但是国家安全部工作小组以此次行动属特殊任务、工作小组来藏属于机密行动、不便公开为理由拒绝了武警临时指挥部的这一要求。<br />
<br />
　　七日下午，驻藏武警临时指挥部向成都武警总队和全国武警总部报告了拉萨方面的情况，并请求中央方面对此次行动予以表态。就在武警临时指挥部向全国武警总部发报的同时，中央办公厅、中央军委办公厅、中共中央统战部、总参谋部、新华通讯总社、国家安全部和公安部等中央级部门也受到了从拉萨各系统发来的报告和事件说明。<br />
<br />
　　三月七日上午，武警士兵六、七人一组，沿街搜查参加过抵抗的藏人。藏人在严酷的打击下完全屈服了，几乎没有人敢于反抗搜查。成群的受到怀疑的藏族青年、妇女、老人和孩子被押往位于市中心的藏甘贸易中心大楼，在那里接受临时指挥部的审问。拷打声、叫骂声和惨叫声不断从大楼二层大厅传出来，大约有八名藏人在这里被拷打致死。另外在其他街区，有人因为身上没有证件而被武警当场打死。甚至有三名公安厅的便衣也被武警搜索队打成了残废。中午十二点左右，公安厅电话警告武警临时指挥部：清查部队应立即停止公开杀人，并提出人犯的收押工作应由公安系统全权负责，武警无权单方面处理。<br />
<br />
　　三月七日下午，一队武警在市区检查过往行人时，发现有十多名藏人没有合法证件。当他们正准备将这些人押往临时指挥部时，有两名藏人逃跑，一名藏人被当场击毙，另一名被追赶上来的士兵摔倒。当这名士兵用枪托狠砸那个逃亡者头部的时候，不慎扣动了扳机，一梭子子弹射出来将身后的一名士兵打倒，子弹全部打中头部，这名中弹士兵当即死亡。当时在场的所有武警士兵都傻了眼，愣愣地站在那里不知如何是好。带队的军官是个矮子，面对这种情况沉思了片刻，然后向他身后的士兵要来冲锋枪，命令躺在地上的那名逃亡藏人站起来。这位藏人被打得很厉害，他挣扎着从地上撑起身体。军官走了过去，提起冲锋枪对着那位藏人的脸部扣动了扳机，十几发子弹将那人的脸全部打烂。军官击毙了那个藏人，转过身对其他士兵说：“杀人犯被正法了，现在我们替他（指那位被自己人误杀的士兵）报了仇。回去后，你们必须照这个情况讲，谁要是胡说八道，小心老子对他不客气……”<br />
<br />
　　“一名士兵被藏人开枪杀死了”！武警临时指挥部里的大小军官们为之振奋。在当天报给中央和总部的材料中，他们把死亡士兵和那位无辜被杀死的“凶手”照片放得很大，并在报告中大肆渲染：藏人在这次暴乱中大量地使用了枪械。为了把这件事编得更加合理，他们把平时没收的一些鸟枪、气枪和双管猎枪摆了出来，放上许多自动步枪子弹和雷管，拍摄了录像带和照片报至中共中央和武警总部。<br />
<br />
　　这的确是个惊人的消息。公安厅和其他部门一时不知如何对待这突然爆出的“冷门”。道理很简单：既然藏人在如此打击下还敢动用武器还击，那么，武警方面的屠杀当然是不可避免和可以理解的。面对着摆在前面的这些照片，素有“鹰眼”之称的公安厅、局的那些人们竟没有看出那全然是经不起推敲的一堆七拼八凑的破烂。公安系统对武警的态度首先转了风向，在向自治区党委和公安部报告时承认了这些证据的可靠性。西藏电视台在新闻节目中反复地播出了这些“证据”，用以说明镇压的必要性。<br />
<br />
　　面对这些“证据”，北京方面的一些局外人首先提出了疑问。新华社“内参”编辑部人员在发稿时提出：这些枪和子弹是不能配套使用的，而且那位死亡的士兵是被何种枪弹致死在武警方面的证据中并没有明确说明，“这里面有明显的漏洞，我们不能发稿！”一石击破水中天，怀疑从北京方面立刻蔓延到拉萨各界。乔石慌了，他在电话中指责武警的笨拙，同时命令立刻取消这些伪证的报道，越快越好。武警方面万万没想到此事会弄巧成拙，引起了如此巨大的反映，他们赶快派人去电视台取回了那些所谓的证据材料。但此时这则笑话已经在拉萨各界传开了，西藏政界有人断定：武警这回输定了。<br />
<br />
　　三月七日晚间，在自治区党委主持的联席会议上，各界都以嘲弄的眼光注视着武警系统的与会者。武警临时指挥部的负责人如坐针毡，一言不发硬著头皮准备抵挡各方面对此次镇压行动的强烈指难和攻击。他们在挨时间，他们希望中共中央能尽快对此次行动表态。在其他部门与会者怀有敌意的交头接耳声中，他们挨过了会议开始前最初的三十分钟。<br />
<br />
　　会议宣布开始，自治区党委第一书记胡锦涛首先宣读中共中央、中央军委对此次事件的联合指示：<br />
<br />
　　一、此次平乱对维护祖国统一、维护西藏地区的安定团结起到了积极作用，对一小撮破坏祖国统一的分裂主义分子给予了有力的打击，效果是良好的。<br />
<br />
　　二、武警驻藏部队在这次平乱行动中，坚决执行中央指示，不怕流血牺牲、英勇顽强，胜利地完成了中央布置的任务，彻底地平息了一小撮分裂主义分子制造的暴乱，应予以嘉奖。对在平乱中受伤和牺牲的官兵，应予以抚恤和慰问并记大功。<br />
<br />
　　三、西藏自治区党、政、军各部门应发动群众，总结经验，加强各界对维护祖国统一、维护安定团结的认识。要加强舆论宣传工作，大张旗鼓地宣传统一的方针，要旗帜鲜明地对一小撮分裂主义分子妄图分裂祖国的阴谋进行不调和的斗争。<br />
<br />
　　四、加强党、政、军和各部门之间的团结，要坚决杜绝宗派主义和本位主义在各部门的影响。同时要对党员干部大力开展理想道德的教育，树立共产主义世界观。要使广大党员干部严格遵守党和国家制定的纪律和法规，做到不利于团结的话不说，不利于团结的事不做。<br />
<br />
　　五、自治区党、政、军各部门要步调一致，广泛开展爱国主义教育，要坚持不懈地宣传中国共产党在西藏社会主义建设中的领导地位，同时要大力宣传西藏经济、教育、科技等方面的巨大成就。<br />
<br />
　　六、公安系统应继续配合驻藏武警部队的清查工作，要坚决、彻底、干净、全部地消灭暗藏在西藏地区的反动势力，以保证社会主义事业在西藏的健康发展。在清查过程中要严格执行党的政策，坚持教育大多数、孤立和打击一小撮的方针，避免清查扩大化，造成群众的恐惧心理。<br />
<br />
　　七、对参加此次骚乱的大多数藏人，采取教育为主的方针，但一定要有认罪态度并保证不再重犯。对悔改表现较为突出者，应予以鼓励和给予适当奖赏。对在骚乱中致死的藏人亲属应向他们表明骚乱不得人心和坚决维护祖国统一的立场，争取这些人立场有所转变。有关方面在其亲属立场转变时可适当给予抚恤和照顾。对在骚乱中受伤的藏人，应本着人道主义精神根据不同情况分别安排治疗。要尽可能地做好善后工作。<br />
<br />
　　八、对宗教界的清查要慎重，切勿情绪化。对一些寺庙的分裂活动，要继续坚持有理、有力、有节的斗争方式。遇事要多同主管部门协商，不要随意闯入寺庙，以免为分裂主义分子提供制造矛盾的借口。<br />
<br />
　　九、中央决定在西藏拉萨实行戒严，戒严令于一九八九年三月八日由国务院总理发布，各单位要做好各项工作准备，配合戒严令的实施。关于戒严令实施的具体步骤见国家安全部、总参谋部的通知。<br />
<br />
　　中共中央、中央军委联合指示中的态度向人们暗示出这场悲剧的真正导演者。它象一股注入反应釜中的中和剂，使大量翻滚起来的庞杂物质又重新沉淀了。武警系统的负责人露出了几天以来的第一次笑容。令人头晕目眩的政治轮盘终于停止了转动，指针停在了他们所要的位置上。他们又一次地成了政治上的赢家。<br />
<br />
　　联席会议的最后一项是由国家安全工作小组宣布国家安全局和总参谋部共同发布的。<br />
<br />
　　联合戒严通知，大致内容如下：<br />
<br />
　　在八日戒严令发布后，国防军五十二旅和武警驻藏部队联合担任拉萨地区的戒严任务。武警部队沿北京路、林廓路、青年路一线布防，负责成关区市中心的戒严，五十二旅负责除成关区以外的市区和郊区的全线防务。当雄、贡噶机场由武警和五十二旅联合防守。武警部队负责的临时指挥部宣布撤销，由五十二旅和武警部队共同组成戒严指挥部。中央军委和国务院对指挥部具有绝对指挥权。目前五十二旅正在开进途中，武警部队要严守岗位，并着手移交工作。接防工作要求于八日中午十二时以前结束。另外在此次骚乱中被捕及关押的所有人犯，应归口处理，临时指挥部在撤销之前必须将死伤人数和审讯结果报告中央并向自治区公安厅着手接交工作。<br />
<br />
　　在这一期间如发生意外事件，任何部门都不得擅自处理，必须及时向中央有关部门请示，待得到确切答复后才可行动。公安厅系统要充分动员起来，配合戒严部队完成戒严任务。同时严格担负起社会治安和清查户口的任务，对在此次骚乱中被暴徒抢砸和烧毁的公共、私人设施，应尽快组织人员进行修护工作，对市容清理和整顿的工作要加快工作步伐。自治区人民政府所属各商业和经济部门，应尽量保证戒严部队的日常生活供应，使戒严任务顺利完成。<br />
<br />
　　七日夜间，拉萨市区到处都能听到哭声，很多藏人居民的家里都有人死伤和被关押。街上还有三五成群的藏人在往火堆中扔自行车和散落在街上的其他商店物资，当武警巡逻队开过来时，人们才靠向马路的两旁。有人偷偷地向武警吐口水，但没有人敢公开叫骂了……<br />
<br />
　　一些藏族的人大代表和政协委员组织起来，结伙成群地到自治区人大和政协机关，要求与这两个部门的负责人见面，要求中共中央方面对这次过火的镇压行为予以解释。他们大多数情绪异常激动，有的竟在办公楼前大声痛哭起来。<br />
<br />
　　有关部门的负责人一直没有出来和他们见面谈话。面对这种状况，政府部门和统战部门的负责人都在躲。他们自己知道，没有任何理由可以说明这些前来的藏族委员们。<br />
<br />
　　一些在自治区党、政部门工作的藏族干部因家中有人在镇压中死伤和被捕，他们也串联起来向有关方面要人并向自己所在单位提出辞呈。<br />
<br />
　　一时间，自治区党委和自治区政府办公大楼里呈现出一片混乱和紧张的情绪。不满的声音是从内部爆发出来的，自治区最高当局似乎已经失去运作的能力。他们中有人断言：这场镇压为今后的西藏政局种下了祸根，目前所造成的状况将使西藏社会从此失去安定。这里的每个藏人都有可能成为一颗随时都会炸响的炸弹。一些早年调入西藏工作的内地干部为此流下了眼泪，他们万万没有想到事情会搞成这个样子。面对拉萨人那充满深仇大恨的眼睛，他们知道：自己进入西藏的光荣乐章已经打上了休止符。因此，他们中很多人准备调回内地工作，他们预感到：真正的危险已开始降临。<br />
<br />
　　这天深夜，哲蚌寺、乃穷寺、色拉寺和大、小昭寺在同一时候吹起了法螺并有节奏地敲击皮鼓，众僧尼咏吟着《密法金刚经》，开始了对死者的超度和对杀者的诅咒。在听到这声音的同时，拉萨的藏人也在家中摆起了酥油灯祭坛，插上了高香，齐声合咏。这声音凄历而低沉，和著法螺和皮鼓升入了漆黑的夜空。这声音不绝于缕地笼罩着拉萨城从深夜直到天明。这声音使人听起来感到毛骨悚然――天神震怒了么？<br />
