　　二月八日夜里，驻拉萨市武警二分队受名前往大昭寺摘除“雪山狮子旗”。当他们到达人民广场时，发现旗帜已经消失，这使武警方面大为震怒，遂下令二分队进入大昭寺以追查反革命证据为理由进行清查（这是自一九八八年三月以来武警第二次进入该寺）。九日清晨，武警从寺中带走二十余人，并警告寺内堪布主持：如果再有挂旗事件发生，全寺僧人将悉数逮捕，立即查封该寺。<br />
<br />
　　十日上午，大昭寺派人召集了哲蚌寺、乃穷寺、色拉寺的堪布主持及西藏宗教界头面人物数十人前往自治区政协告状，要求政府尽快释放被捕僧人，赔礼道歉于大昭寺；并提出因武警擅闯寺庙导致寺内物品丢失的问题，希望政府能够清查后归还。对于拉萨武警部队勾结拉萨工商局人员在八角街的违法乱纪行为，再次提出了大量证据，要求有关方面从严查处。西藏政协主席兼自治区党委副书记热地与宗教界代表一直谈到下午四时，政协方面表示：将把这些问题提交有关方面解决，并希望各寺对近来的反政府情绪不要和中加油，应采取和政府合作的态度，共同协商解决目前西藏存在的各种问题。<br />
<br />
　　在处理宗教界近年来出现的反政府情绪这一问题上，中共西藏地方政权始终采取一种谨慎的态度，这种态度似乎受制于班禅的影响――自从一九八四年以来，班禅一直在各种场合表示：希望中共能彻底地检讨数十年来在西藏地区造成的极左影响，并一再呼吁中央修补对西藏的政策。由于班禅公开指出过去三十多年来中共对西藏的政策失误，造成了西藏文化、宗教、经济、生态、人口等一系列问题，致使西藏宗教界开始敢於在政府内部的会议上和其他宗教场合上突破三十年来的言论禁区，为自己的利益直抒其言，甚至对十年来内地的经济改革也提出了很多批评。为此，中共西藏政府内部也对内地经济改革使西藏地方经济受到危害这一现象屡有微词。到了一九八七年班禅公开表示准备向达赖让位，欢迎达赖回国主政。从表面上看，似乎班禅的态度是为了迎合中共的统战政策。但实际上，从班禅近年来一系列的态度言论分析，他的这种表示无非是宣布了他另劈蹊径的政治动机。就笔者看来，他的一系列表现和他于一九八九年一月二十八日突然死亡是互为因果的，也许这将永远是个谜了。<br />
<br />
　　面对上述这种情况，中共中央在这个问题上有两种不同意见。一种意见认为：鉴于一九八七年九月到一九八八年三月拉萨地区连续发生了六次大规模的群众抗议示威活动这一现实，应重视以班禅为首的西藏宗教界的意见，改进对西藏地区的工作方针，重新考虑对西藏地区的经济支持和各项政策安排。对于西藏这一特殊地区，应从历史的角度考虑宗教界的地位和影响，对达赖流亡海外这一问题应有一个较为开明的政策。对于一些地方主义、民族主义的情绪及由此引发的动荡，应采取克制的态度，应避免发生流血冲突；另外，对驻藏的党、政、军部门的经商问题应下大气力解决，对武警部队在拉萨地区军风、军纪要严加整顿，由此解决内地的改革对西藏造成的不利影响。<br />
<br />
　　另一种意见则与之截然相反。即：西藏问题的关键是宗教界的反中共反中央倾向，应该把近年来出现的一系列反共活动看成是达赖集团在西藏的政治渗透，在这个问题上没有调和的余地。这种意见的根据是中印边界历年来紧张的对峙局面和达赖近年来频繁的国际活动。并以此提出对班禅近年的反政府和反共言论应有所限制，不宜无原则地扩大他在西藏地区的影响。对西藏近年出现的动荡，应以一九五九年的成功经验为政策准绳，坚持不懈地镇压宗教界内的反共势力；不应因顾忌国际舆论向宗教界做政治让步，使反共势力在西藏从新抬头。从近年来的中共民族政策理论刊物上均可以看到这两种不同意见的分歧和争论；在中共中央和西藏地方党政军来往的文件及指示中，也可以明显看出，对于如何处理西藏，中共内部始终存在着很激烈的矛盾和斗争。（关于中共内部对于西藏问题的分歧和矛盾及历史原因，作者将另文阐述，在此不详细讨论）。<br />
<br />
　　在这种背景下，西藏在八九年二月呈现出一种异常紧张的对立状态。由于宗教界及藏人对挂旗事件后军警进入大昭寺搜查并逮捕僧人的反映普遍强烈，加之中共西藏政府在有限的时间内对这一事件没有做出应有的良性处理，致使拉萨地区从二月一十三日到三月初的这段日子里，出现了四次规模不等的反政府示威活动。西藏其他地区如日喀则、那曲，昌都等地也出现了不同程度的骚动。加之届年一月二十八日班禅喇嘛在日喀则的突然去世，西藏宗教界由于失去了现存的宗教主宰，反抗的情绪更加趋向具体化。人们在拉萨二月份的四次反政府游行和挂旗事件的激励下，更加强了对中共政权的离心倾向――藏人们渐渐地习惯于怀疑和争论，街头巷尾的甜茶馆成了政治情况的交流场所，从抨击毛时代的残酷斗争转向抱怨现政权的不公平。<br />
<br />
　　三十多年来中共对西藏的失败政治使人们渐渐地眷恋起昔日噶厦政府时代旧规范的威严。随着八七年再次公开反抗中共政权运动的兴起，西藏地区的汉藏矛盾逐渐升级为一场政治斗争，统治者和被统治者仿佛都走到了某种极限。在人们的诅咒声、抱怨声和谩骂声中，一种巨大的反抗中共的情绪，一种挟裹着昔日宗教温馨和未来幻想的新的非理性，在一九八九年三月二日拉开了悲剧的帷幕。<br />
<br />
　　三月二日上午，拉萨大昭寺正面的人民广场上突然挤满了人群。人们不时的低语，目光向大昭寺正门望著，似乎都在等着什么。十点左右，有十多名喇嘛和二十几名尼姑从人们左侧沿着八角街转经道口走进人民广场。人们立刻为他们让开了一条道路，他们一边走一边向围观的人们招手并用藏语高声呼喊着口号：“坚决要求严惩迫害宗教人士的凶手！”“处死杀害藏人的武警！”（这里是指在一九八八年三月的独立运动中被武警用棍棒和石块儿打死的七名喇嘛和十三名藏人）随着这几十名僧尼穿越人群进入广场，站在一旁的人们开始向他们涌去，一时出现了准备合流的态势。突然，此时从八角街转往道口和人民广场对面的青年路口涌出了两支近两百人的便衣警察，他们很迅速地组成了一道人墙，将游行的僧侣和围观的群众隔开，并大声地威胁准备走上前去的人们。人潮被阻在了广场边缘，许多人曾试图越过便衣们布置的防线，但都被他们粗暴地驱赶回来。<br />
<br />
　　几名中外游客站在广场较高的台阶上端起照相机很迅速地摄下了这一场面，但立刻就被数十名便衣揪了下来。那几个国内的游客被当即搜身，随后被押往八角街的派出所；其余的几个外国游人赶忙高举着自己的护照，一时间便衣们不知如何是好。一名便衣警察（大概是拉萨公安局外事科人员）操着英语向他们说：“为了保护你们的安全，请你们立即离开这里！”但这些外国游客都站着不动，并没有按照他的意思离开广场。<br />
<br />
　　人民广场上的人静下来了，由于便衣警察的防线非常严密，广场靠向马路的一旁变得空空荡荡，只有那几十名僧尼还在高喊着口号。他们等待人们合流的愿望没有实现，便毅然向人民路走去。当他们走在马路上的时候，路上的行人开始向他们招手，即而有人走上前去尾随在他们队伍的后面，立刻这支队伍扩大到了三百多人左右。这些僧尼和藏人高呼着口号，从人民路延雪城旅馆折回，又向人民广场走来。二十分钟後，广场上由便衣警察组成的防线开始被人们拥挤，游行的队伍与广场上的围观群众汇合在一起了，人群中爆发出阵阵欢呼声。一个喇嘛高呼：“坚决要求达赖喇嘛回藏主教！”“还权于藏人！”声音落下处，全场全体藏人齐声重复了这两句口号。又有人高呼：“打倒贪官污吏的汉人政权！”在场人们又异口同声的回应。此时广场上人山人海，口号此起彼伏。一些年轻的藏人开始寻找刚才的那些便衣警察时，发现他们已经消失的无影无踪了。<br />
<br />
　　就在这时，广场对面有人高喊：“武警来了！武警来了！快散……！”一些站在广场中心的人们听到这个消息，开始往外面挤，人群骚动起来。那几十名僧尼被一些人簇拥着退到八角街转经道里，消失在熙熙攘攘的人群中……<br />
<br />
　　武警并没有出现；僧尼们也没有再回到广场上来，广场上只剩下了看热闹的人们。这些人不愿散去，他们还在望着八角街转经道口和大昭寺正（木詹）上方，希望能再看到这地方能挂出来象征独立运动的雪山狮子旗来。不少中外游客还站在广场台阶的高处举着照相机。已经是下午一点钟左右，人们还在期待着一个新的兴奋，希望今天能够再出现一个上午那样的高潮。但是，人们失望了，高潮并没有出现……很久很久，广场上的人群在议论中渐渐地疲倦了，人群开始逐渐稀疏，傍晚时分，广场恢复了往日的平静。今天拉萨的人们似乎对中共西藏当局没有做出强硬的姿态而感到不满足。<br />
<br />
　　直到深夜，还有不少年轻的藏人聚集在大昭寺前不肯离去。这是一股可怕的情绪，它酝酿着反叛。<br />
<br />
