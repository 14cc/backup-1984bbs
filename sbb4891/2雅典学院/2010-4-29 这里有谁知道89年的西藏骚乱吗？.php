耻辱的和平<br />
<br />
　　八日清晨，位于拉萨市西郊五公里处的西藏第一大寺――哲蚌寺，在主寺噶丹颇章（宫）顶上的高大“塔经”，上升起了一面巨大的“雪山狮子旗”。全寺七千僧人站在大旗之下，在皮鼓和腿骨号角伴奏声中，最后的齐声咏唱了《智者喜筵》中的英雄篇章：仁者以智慧保护自己，敌人再多也难以伤害他。乌仗那婆罗门的儿子，一个人就能降服所有的敌人。大人物所敬仰的，卑贱者却去轻侮。顶月神头上的月亮，却成了阿修罗的食品。不能专心的修持者，难以有得道成佛的机会。要是能持守寂灵，得道就为期不远了……咏唱持续了近两个小时，当太阳升起来的时候，咏唱结束了，七千僧人全部龟缩进了那庞大而神秘的建筑中。四处山野一片安籁沉寂，只有那面旗帜迎着朝阳，合着鸟儿的叫声，在高高的噶丹颇章顶上随风飘舞――耻辱的和平开始了。<br />
<br />
　　八日九点半左右，武警部队三百名士兵包围了哲蚌寺。他们用手提话筒向哲蚌寺喊话，武警限僧侣们二十分钟之内撤下“雪山狮子旗”。二十分钟过去了，僧侣们没有行动。三百名武警士兵在一名中级军官的率领下，鱼贯进入哲蚌寺，沿着高高的台阶登上了主寺噶丹颇章。一名武警士兵飞快地爬上了“塔经”，摘下了象征独立的旗帜――全寺静极了，七千名僧侣默默地注视着武警们的一举一动，没有任何反应。当武警三百名士兵情绪紧张地走出哲蚌寺的时候，前来接防的五十二旅的连队也到达了该寺。十分钟后，换防的交接完成了。武警在交接中没有告诉对方刚才发生的事情，他们捧着“战利品”回去，准备向上级报功。<br />
<br />
　　八日十点整，国防军五十二旅的部队从东、西、南、北四个方向同时开进拉萨市郊，在完成了城区外线布防后，开始进入市区接防。在市区他们受到了武警方面的阻碍；武警方面表示：由于目前市区内正在进行武警公安系统的武装示威游行，为了保证秩序，所有换防事宜暂缓执行。<br />
<br />
　　八日十点半，由数十辆载满士兵的卡车和成百辆吉普车、跨斗摩托车组成的武警公安系统车队，沿拉萨市各主要街道，进行了一场声势浩大的武装示威游行。一时间，警笛长鸣、口号震天、马达轰鸣，拉萨城在一片刺耳的声音中颤动。武警系统在换防的最后时刻，向前来接防的军队和全体拉萨人民显示着他们的存在，以表明他们仍然对西藏的局势起着举足轻重的作用。<br />
<br />
　　八日十点整，中央人民广播电台、中央电视台、西藏人民广播电台、西藏电视台同时播送了由中共国务院总理李鹏发布的对西藏拉萨的戒严令。拉萨城区的大街小巷一时挤满了各单位的广播车，高音喇叭的巨大音量相互交错地轰击着这座城市，没有任何办法听清戒严令的内容。整整一天，拉萨人民都浸泡在这种噪音之中。<br />
<br />
　　三月九日，由西藏自治区党委为首的西藏党、政、军、青、工、妇各界，根据中共中央、中央军委联合指示的精神，开始组织慰问团到医院和军营去对武警军人进行慰问。西藏电台、电视台连日报道着各种各样的慰问活动；连对此次镇压意见最大的政协也赶忙表态，连日召开座谈会。宗教界和社会贤达们在这种人人过关的形势逼迫下纷纷表态支持这次镇压行动，并象演戏一样地在镜头前一一亮相。拉萨人沉默了，西藏也沉默了，只有一个喉咙在叫，拉萨城象一个小丑一样被那些化装大师们涂抹着。刽子手们在这种涂抹中更衬出了他们那“血染的风采”。<br />
<br />
　　下面是西藏人民广播电台和西藏电视台三月十日至十二日播发的几则消息摘要：<br />
<br />
　　三月十日《西藏日报》评论员文章《稳定局势靠什么？》“……无数事实证明了一个伟大的真理：西藏是伟大祖国的一部分。西藏的天是西藏人民的天，西藏的天是变不了的，……中国共产党的领导是西藏社会稳定发展的根本保障，……今后有谁胆敢在西藏搞分裂、闹变天，中国共产党必将带领西藏各族人民给他们以毁灭性的打击”<br />
<br />
　　三月十一日，西藏电台记者杨小芳、张金报道：“少数分裂主义分子的倒行逆施不仅危害了广大人民生命财产的安全，破坏了人民安宁的生活，也使广大信教群众心灵上蒙上了阴影。往日教徒如云、香烟缭绕的大昭寺周围，劫后一片惨败景象。就连转经道东北方向的巨大“塔经”也被烧得只剩一截黑木椿。人们不难想象，这一小撮分裂主义分子，口中狂叫保证宗教信仰自由，而干的却是烧、砸、抢劫宗教场所的罪恶勾当……”<br />
<br />
　　三月十一日，拉萨电视台记者余文潮、陈重报道：“……连日来，在拉萨的公安、武警和解放军战士在搜寻参与拉萨骚乱的分裂主义分子时，搜缴了一批枪枝弹药和反动宣传品；自国务院发布戒严令以来，在拉萨的公安、武警和解放军战士立即行动起来，迅速搜捕在这次骚乱事件中证据确凿的打、砸、抢劫分子。在搜捕中，查获了部份枪枝弹药。其中大部份是自制的步枪和老枪改造成能适用新式步枪子弹的枪枝……这次查获的有大量新式步枪子弹、雷管、火药及大量的反动宣传品……”<br />
<br />
　　三月十二日，西藏电台记者杨小芳报道：“按照自治区人民政府第一号令，拉萨市公安外管部门妥善安排在拉萨境内的外国人以保证他们在戒严期的安全，从三月八日戒严令发出到九日晚二十时三十分，已有一百九十八名在拉萨旅游的外国人分六批离开拉萨戒严区，其中有三十八人前往樟木口岸离境，剩下的小部份外国人也将在近日离开拉萨。据了解，在这次少数分裂主义者制造的骚乱中，有十名外国人不听我有关方面劝阻，违反了中华人民共和国外国人出境入境管理法和外交部制定的外国驻华记者须知以及拉萨市人民政府一九八七年发布的第三号通告的有关规定，被我有关部门分别进行了批评教育、警告和罚款的处理，其中两名英国记者已于八日晚被我外管部门责令限时离开西藏。”<br />
<br />
　　三月的这场事件引起了国内外新闻界的极大关注，他们纷纷派出自己的记者前往拉萨进行公开和秘密的采访。由于拉萨地区实行戒严，国内外的记者在中国西北、西南靠近西藏的省份沉淀下来。于是，四川省会成都成了新闻界聚会的中心，成都锦江饭店的大厅里，无数新闻专业人员交头接耳，四下忙碌着交换和传达一些并不可靠的消息。八日以来，只有几个美国记者搞到了一些远距离拍摄的照片，画面是正在向拉萨开进途中的五十二旅的部队。没有拉萨事件的任何真实消息传出来。<br />
<br />
　　有些国内记者试图凭着记者证和介绍信去购买前往拉萨的机票，当他们到西南航空公司售票处时，才知道必须凭西藏自治区人民政府和西藏军区驻成都办事处的介绍信才能购票。他们又奔向这两个办事处，在那里，他们看到有成百人在等待着办理去拉萨的介绍信。负责审查的人员告诉他们，只有三种人有可能进藏：拉萨的固定居民、西藏党、政、军系统的出差人员和有部级证明的中央及地方各部门的出差人员。十日的班机，全机只载了三十二名旅客，西南联航的客运在这种情况下不得不捎带着装货。国内外新闻界的机灵鬼们这才明白：拉萨是真的戒严了，她与外界已经完全隔绝。<br />
<br />
　　拉萨城内，搜捕还在继续进行，每天都有大量的藏人因涉嫌参与骚乱而被公安武警收押。有些藏族青年甚至因为曾经和工厂党支部书记吵过架也被武警带走审查。武警士兵继续在八角街戒严区随便闯入民宅，随便抓人。十一日上午，一名藏族工人骑着摩托车行至路口检查站，因煞车时超过了警戒线而被武警士兵开枪打伤。藏人们在街道上行走随时都有可能被盘查，满街都是头戴钢盔的军警。一些内地商人开始试探着在街上摆摊售货，但一天下来，几乎没有什么人光顾。拉萨变成了一座只有军警的死城。<br />
<br />
　　三月十一日，自治区公安厅和西藏军区联合向自治区党委报告了自三月五日以来市民的伤亡及被押人数：“截止三月十日，拉萨市民共有三百八十七人在骚乱中丧生，其中多数是被枪弹击毙致死的。七百二十一人在骚乱中受伤，其中伤势较严重者一百二十六人。三百五十四人失踪。两千一百多人被捕或在押。宗教界人士八十二人死亡，三十七人受伤，一百多人失踪，六百五十人左右被捕或在押。外地藏人死伤数正在统计中。武警方面到昨日（三月十日）已向公安厅、局系统移交了一千二百三十六名被捕人犯，其他人犯仍在武警处关押……”<br />
<br />
　　这份报告交上去的当天就被定为绝密材料由国家安全部工作小组封存，任何部门都不得查阅。但这是徒劳的，各单位都有自己的统计系统。<br />
<br />
　　很多部门争相向自治区公安厅系统核对藏人的死伤及关押人数，但公安厅为此只能保持沉默。其实，在这场镇压中究竟有多少人死亡，公安厅的人也不清楚，因为他们对外地藏人在拉萨的人数一无所知，那些报告上的统计数字都是从拉萨市公安局的户籍册中得来的。西藏历史中又有一页被牢牢地粘了起来，成了千古之谜。<br />
<br />
　　三月十二日清晨。有人在拉萨河上发现了大量被砍成碎块儿的尸体。当市公安局的人赶到现场时，尸体碎块儿大部份都漂向了下游，河边水草旁挂满残存的肢体和内脏……不知道这是死者家属们干的还是他人所为。<br />
<br />
　　当这件事在拉萨城里的甜茶馆传开的时候，在场的藏人们表现出一种惊人的沉默。<br />
<br />
　　难道他们真的被那种巨大的恐怖击得麻木了吗？――这个民族的文化深层中潜藏着一种因果报应的苯数哲理，难道他们已经把命运托付给来世了？<br />
<br />
　　当你看著这些呆滞的面孔时，你会感到不寒而栗，因为你会得到这样一种回答：“既然你们赢得了今天，那么，我们就选择明天吧！”<br />
<br />
　　明天会是什么样子――死者和生者都无法回答。法螺和皮鼓又一次从古老的寺院中响起，僧侣们又开始咏吟那晦涩而深奥的经典。这声音被山风托起送上了四外那褐色的山峦。夕阳落在了远山的背后，夜的巨幕正在徐徐落下――拉萨悲剧的最后一幕在千古永存的青藏高原上宣告终场。<br />
<br />
　　两个多月后，在那个政权的心脏――北京，一场更大规模的屠杀开始了。<br />
