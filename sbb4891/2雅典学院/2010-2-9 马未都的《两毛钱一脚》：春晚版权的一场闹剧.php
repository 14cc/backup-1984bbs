大约在一个多月前，有朋友不断问我：“你给春晚写小品啦？”我以“怎么可能”作答；后过了些日子，一家报纸上载文说春晚语言组用我的一篇小文改成小品，效果颇好，我虽奇怪怎么春晚无人与我联系，但亦一笑了之；可后来发生的事情却让我再也笑不起来。<br />
<br />
<br />
在消息见报之后，先是有人自报家门（春晚剧组）不报姓名地问我，是不是写了如此一篇文章，然后又传来一份资料让我看看是不是一样。这是刊载在2009年 11月下半月刊的上海《故事会》，署名“陈志宏”的一篇抄袭之作，我想两文读后结论自有。我的小文首发于2008年8月14日的新浪博客，2009年2月人民文学出版社结集出版《马未都说・车上篇》第36页，名为《量力而行》。其后经过三番五次的电话盘问，我都如实禀报，电话另一头剧组小姐总是将信将疑地说出不算悦耳的话。我年长，她年幼，我不和她计较。可她后来的话比较刺人，她告诉我说：“我们（春晚语言组）本来是根据陈志宏在《故事会》的文章改编的，可没想到您也写了一篇。”我听这话确实也糊涂了，我怎么早在他之前“也写了一篇”？好像有点儿不该的意思。<br />
<br />
<br />
后来，剧组小姐又打电话告之：“陈先生愿意与你共同署名。”我只好告诉她，我不认识这个陈先生，我不追究他抄袭是我大度，但我不可能“共同署名”。我当时还退了一步，我说“让他在改编里署名吧，我也不计较了。”谁知剧组小姐郑重告诉我说：“你理解错了，我们改编另有别人。”<br />
<br />
后来，剧组小姐电话通知我，并发一邮件《确认书》让我确认同意改编，然后用快递给春晚剧组寄回。我在我的权利未弄得十分清楚的前提下，主要是怕被蒙着“共同署名”，没有及时寄出；剧组小姐就电话急催，毫不知道什么叫客气，从这事发生我接了她几十通电话，有时甚至在半夜，但我总是耐着性子听她那番道理。我告诉她，《确认书》是个单方面的凭证，签字后如寄出，我手头就没有任何凭证，此事应该签合约，双方各执一份。她先告诉我春晚几百份都是这样签的，没有其他法律文本。我说那我不签，她这时只好说去问问再告诉我。<br />
<br />
<br />
过了几天，她又打电话来告诉我说可以签合约，不签《确认书》了，马上发合约范本给我。我收到合约后把空格的地方一一问了她，她告诉我说空格不要填，只签名寄出就可以了。这事很奇怪，天下的合约都没这么签的，要命的地方都空着，让乙方（即我方）签字，那这合约有什么意义呢？！我问她一个文人难以启齿的问题，空格内的“作品许可使用费”金额是多少呢？她告诉我1000到2000元，每个人都一样，节目播出后就给。<br />
<br />
<br />
接下来的事有点儿匪夷所思。我告诉她这样的合约我不太想签，主要是……，剧组小姐有点儿着急上火，口气不太友好：“我告诉您吧，这节目上不上还不一定呢，签不签随便。”说完把电话挂了。我愣了半天神才省悟过来，估计所有作者都特想为春晚增光，为自己积累，上不了春晚等于白瞎，所有作者都有求于人，剧组的工作人员盛气凌人也是有原因的。可这招对我不管用，这个节目上不上春晚，我一点儿都不在乎。<br />
<br />
<br />
后来，我的电话就没断过说客，连我都记不起的发小也打来电话，嘘寒问暖，当我如实招供后，他们也觉得解铃还需系铃人，又让剧组小姐找我赔礼道歉。按说我再次接她的电话就是原谅了她不恰当的做法。她和我约了时间，到观复博物馆找我，她在路上电话指示我，让我自己先打印合约5份，她亲自来取。我觉得我终于可以不用自己花快递费了，她迟到了，我心里依然以路不好走为她解释。<br />
<br />
<br />
当我们终于面对面坐在会议室里，剧组小姐向我致歉，她说：“我不知道您的身份，我要早知道就不会这样。”我告诉她：我是谁不重要，重要的是你们应该尊重每一位作者，不管他是谁。我后来出出进进打印文件，她跷着二郎腿坐在屋里一动不动，我心里依然用她可能上学时缺乏社交礼仪教育为她解释。可当我跟她说，合约某些条款我不同意，如她不能做主，让她回去商量时，她突然不悦，告诉我这样她的饭碗就会丢了；这让我内疚，进退维谷。我只好告诉她合约是一个双方自愿签署的法律文本，一旦生效，不可反悔，所以要慎重云云……<br />
<br />
<br />
再后来发生的事情十分难堪，她开始哭闹，我在她哭闹的间隙跟她说：任何合约都不会因哭闹而签。她不听，仍我行我素。后面的细节及言语限于篇幅，对她也不好，我就不想说了，反正我没签合约。<br />
<br />
<br />
这些天，春晚剧组的人我认识的不认识的，直接的间接的拐弯抹角的都给我打电话，说事关重大，时间紧迫，还是签了吧，否则大家麻烦一团……春晚总导演金越也来电话向我道歉，只是电话里我没法说清至今未签的原因。<br />
<br />
<br />
我想，此时我更应该主张权利，让尊重作者权利不再是一句空话；让本次事件作为社会同类事物的一个范例；让其尊重作者的无形资产成为习惯。我原来的想法是，为了全国人民，我可以不要一分钱让他们改编。但有了这样的经历后，我决定，我要在合约中要求15万元的作品许可使用费，这笔钱如剧组支付给我，我将悉数捐出。<br />
<br />
<br />
我至发此文之时尚未同意春晚语言组改编我的小文《量力而行》（即小品《两毛钱一脚》），尽管他们已花费了大量的人力物力并已彩排通过，但至今未获原作者通过，这不是我个人和剧组的悲哀，而是我们民族习性的悲哀。</div>

							
							
							
							
