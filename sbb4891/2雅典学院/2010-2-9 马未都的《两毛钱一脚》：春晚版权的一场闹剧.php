马未都的两脚<br />
作者：陈志宏<br />
　　新疆水果，天下闻名。<br />
&nbsp; &nbsp; 这里日照充足，空气干燥，为优质水果创造了得天独厚的条件。香梨、西瓜、葡萄、哈密瓜和无花果等等，水灵滋润，甜蜜香馥，好吃得让人叫绝。<br />
&nbsp; &nbsp; 20多年前，马未都先生来到新疆，遍地的水果，让他过足了水果瘾。当时，市场意识极淡，本地水果多为自产自销，便宜得让人觉得是天上掉馅饼了。10公斤香梨，只花了一元钱，吃得马未都肚子溜圆，站都站不起来。<br />
&nbsp; &nbsp; 阿克苏的杏子很多。一天，马未都先生路过一片杏林，大白的杏子挂满树枝，累累硕果，让人一看嘴馋，直流口水。马未都从未见过如此繁盛的白杏，恨不得马上扑上前去采摘，尝尝鲜。但见杏树底下坐着一维族老汉，旁边放了几个铁皮桶，猜想是树的主人。于是，他走上前去，打听白杏怎么卖。<br />
&nbsp; &nbsp; 他问老汉：“请问杏多少钱一斤？”&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 老汉的回答让他有些摸不着头脑：“两毛一脚。”<br />
&nbsp; &nbsp; 马未都把“一脚”错听成“一角”，心里直嘀咕：“两毛就是两角呀，怎么是一角？到底一角还是两角呢？”<br />
&nbsp; &nbsp; 维族老汉见他疑惑，与他解释说：“就是两毛钱让你对着树踹一脚，掉下多少杏子，全都是你的。”<br />
&nbsp; &nbsp; 天下居然有这么浪漫好玩的买卖？<br />
&nbsp; &nbsp; 马未都直乐，交了钱，提着桶，向杏林深处走去。他选了一棵硕大无比的杏树，低垂的枝上挂满了诱人的白杏。心想，只消踹上一脚，必定会有无数的果子掉下来，捡上大半桶，没什么问题。<br />
&nbsp; &nbsp; 他使出吃奶的力气，猛地向杏树踹了一脚，脚腕子都踹痛了，结果，杏树丝毫未动，杏子一粒也没有掉落下来。气急之下，马未都提腿刚想踹第二脚，老汉开口说话了：“再交两毛。”<br />
&nbsp; &nbsp; 这一回，马未都不敢再选大树了，挑了一棵细弱的小杏树，不轻不重地给它一脚，顿时，枝摇杏落，满满地捡了大半桶。<br />
&nbsp; &nbsp; 若干年后，马未都醉心收藏，在买古董宝贝时，偶尔也会犯类似的错误，凭着自己的兴趣，想当然地就购进。但当年在阿克苏给杏树的两脚，还是使马未都先生逐渐形成了自己的收藏个性――头脑要清醒，原则要坚持。<br />
&nbsp; &nbsp; 为人处世，经营人生，头脑不能发热，不能想当然地胡乱踹上一脚，那样，既伤了自己的元气，也不会有任何收获。看清楚自己，洞悉自己的能力，有时，比紧盯目标更重要。<br />
&nbsp; &nbsp; 马未都的两脚，让我们明白，做人的原则――下脚之前，先掂量自己的斤两，不贪心，不虚妄，在最可能成功的地方下脚，一踹踹出真实而沉稳的人生。<br />
