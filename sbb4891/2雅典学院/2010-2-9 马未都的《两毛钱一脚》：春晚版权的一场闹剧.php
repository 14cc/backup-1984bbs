第二百一十九篇・量力而行(2008-08-14 19:45:26) <br />
标签：文化 杂谈&nbsp;&nbsp;分类：2008年 <br />
&nbsp; &nbsp;库尔勒的香梨落地即化，据说远看有梨落地，走近寻不见踪迹，可见香梨之酥脆。我路过库尔勒时，看见卖香梨的老乡，买了十公斤只花了一元钱。我蹲在地上，守着一堆梨肆意尽情地享用。新疆干燥，多数水果处在无菌环境中，所以也不用洗，尽管放心食用。我一直吃到站不起来，才想起要站起来。库尔勒香梨之好吃是不能形容的，只有在新疆库尔勒本地食用，才知其梨甜美。<br />
<br />
&nbsp; &nbsp;新疆的水果都好吃，糖份高，我记忆中的葡萄、西瓜、哈密瓜、香梨、无花果，还有杏，无一不是果香扑鼻，入口甘甜。二十多年前，商品意识淡漠，水果没人贩运，就地消化，所以新疆的水果特别便宜，还以公斤论，差不多都是几分钱一公斤。<br />
<br />
&nbsp; &nbsp;在阿克苏，我路过一片杏树林，满树大白杏，构成一副奇异景观。我年轻的时候，很少看见果树上挂满了果，在白杏压满枝头的杏林面前，我的口水啊，至今一想还满口生津。<br />
<br />
&nbsp; &nbsp;杏树下坐着一维族老汉，身边有几个铁皮桶，我诧异地问他：“杏多少钱一斤？”老汉说：“两毛钱一脚。”我当时还诧异两毛钱是两角，怎么是一角（脚）呢，当我弄清两毛钱允许向杏树踹一脚时，我惊喜天下有这样让人兴奋的买卖。<br />
<br />
&nbsp; &nbsp;我给了老汉两毛钱，拎起一支桶，走向杏林深处。杏树大小不一，我选择一棵硕大无朋，枝头低垂的杏树，铆足了劲儿，背过身像马尥蹶子一样，猛踹杏树，脚腕子都快肿了，结果是一颗杏未落；我刚想再接着踹，老汉说：“再交两毛！”我一下子就老实了，选择了一棵细弱的小树，不轻不重地踹了一下，掉下的杏也捡了半桶。<br />
<br />
