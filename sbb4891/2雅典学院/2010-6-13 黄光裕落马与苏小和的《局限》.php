刘松萝<br />
<br />
1.<br />
听到企业家黄光裕先生落马的消息，我马上就想到了苏小和先生所著《局限――发现中国本土企业的命运》。<br />
<br />
第一次与苏小和见面是在2007年6月北京小西天网友聚会上。那一天，我们从中午11点半一直聊到了下午5点。苏小和给我的印象是一位书生，很文气，彬彬有礼。他很客气地给每一个人赠送了他的新书，6月1日出版的《局限》。他还说，诸位要是有时间，写个书评吧。<br />
<br />
聊天的时候，我就浏览了前言。回家的路上，把前言看完了。到了第二天，就读完了一半。应该说《局限》能够抓住读者的注意力。<br />
<br />
随后，我写了《苏小和君所著《局限》读后（连载）》。写了4200字，基本上是罗列想法，打算以后再修改。由于还有其他的事情，就停下来了。<br />
<br />
2.<br />
《局限》前言的副标题，是《用于实验的一代人》。<br />
<br />
老子有“治大国若烹小鲜”的说法。然而几十年来，中国和中国人一直被看作试验品。无休止的革命、斗争和改造导致了生灵涂炭，活着的人则是惶惶不可终日。最悲惨的是，改革也沿袭了旧的革命思路。没有一定之规，全凭长官的意志。表面看起来日新月异，其实不过是无序而已。<br />
<br />
为此，苏小和把改革与清末的洋务运动进行了对比。他断言：“从清末算起来，中国经济一直在寻求转轨，100多年了，中国的企业家总是自觉补自觉地成为权力的附庸。”<br />
<br />
3.<br />
让人感到惊讶的是，苏小和告诉我们，追求GDP已经到了荒谬的程度。他说：“政府给了企业那么多的保护扶植政策，企业拿什么回报政府？唯一的、也是最好的回报只能是业绩。考核政府的主要指标是GDP，GDP指在该地区所实现的产值，与利润关系不大。”苏小和进一步说，这就很好地解释了为什么中国许多企业在亏损的状况下仍然大干特干，在没有取得利润的状况下仍然狂热地进入世界500强。<br />
<br />
以前只知道在中国目前的经济发展中，得到政府的扶植和获得政府的项目非常重要，万万没有想到这样的发展有时只追求产值，连利润都不考虑了。如果苏小和所说属实，那么我们的经济在很大程度上仍然处在计划经济时代。<br />
<br />
回避了这样的现实，讨论经济改革还有意义吗？</div>

							
							
							
							
