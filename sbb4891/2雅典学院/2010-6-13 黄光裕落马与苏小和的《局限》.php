关于郎咸平张维迎之争，苏小和这样说：<br />
<br />
郎张之争”开始之后，我以为，郎咸平身在香港，对于中国大陆的企业建设，毕竟是门外之人，而郎自己乃财务专家，可能对制度经济学缺乏整体把握。而著名的教授张维迎，也许正如社会上的传言，他已经是一个既得利益者。因为局面非常清晰，只要跳出争论之外，人们很容易就能看清楚，双方都是在说一个问题，这就是制度。郎咸平说了制度的一面，张维迎说了另一面，更多的人是站队，然后附和。<br />
……<br />
郎咸平肯定是看到了公平问题，顾雏军、张维迎等人则看到了效率问题。他们都看到了树木，却有意识的忽视了森林。<br />
……<br />
我一直认为，无论是郎咸平，还是张维迎，他们都应该看到了内在的原因，看到了更高层次的危机。我们无法想象郎咸平身为国际著名财务大家，难道不知道诺斯的“有效率的财产权”？同样，我们也无法想象，出身牛津的张维迎，难道不知道市场经济起码的公平原则？他们都知道，但是他们不说，他们都站在一个狭窄的局部，只是为了自己的利益，在那里喋喋不休。<br />
<br />
应该说，这些看法是很有见地的。我想补充的是，苏小和认为经济学家们不可能看不到问题，只是被狭隘和私利蒙上眼睛罢了。其实，当社会气氛不正常，学术气氛浮躁的时候，学者是有可能犯低级错误的；当学者为利益所迷惑，又没有制约他们的社会力量时，他们的品行和见识有可能下降到我们不敢想象的程度。<br />
<br />
5.<br />
在公平与效率的问题上，苏小和表现出超然的态度。他说：<br />
<br />
中国却一直不能同时解决公平和效率。原因无他，因为我们的体制中有一种对权利和资本制约的错位。一方面是效率不高，另一方面公平损失也没有很好地解决。两个方面都存在。我们对权利和资本两个方面都缺乏有效的制约，所以问题就来了。<br />
<br />
苏小和坚持市场原则，反对郎咸平先生的观点。他说：<br />
<br />
如何有更多的公平，肯定不能像郎咸平说的停止MBO改革，停止产权化的改革，这是行不通的，只会导致效率越来越低，公平问题也解决不了。中国目前的低效率，一个是国有企业的低效率，不能通过郎咸平跟一两个国有企业家谈了话说，他们很努力，因此得出结论是他们的效率很高；不能从短期、局部的企业得出整个国有企业的状况，必须要进一步做全局的分析。<br />
<br />
国家审计署和其他的有关单位曾经做过这样的调查，表明近几年国有企业和国有建设项目投资损失率很高。国家投资的一些大型项目，一些大型的国有企业出现了各种各样的问题：停产、半停产的，达不到设计能力的，出现各种问题的将近50%，这是对资源极大的浪费。为什么会出现这么一个情况？实际上，现代经济学早就说得很清楚了，然而有人不信。比如说存在信息不对称的问题，存在剩余控制权，剩余所有权，国有企业不能很好地解决剩余控制权和剩余所有权如何分配到监控者手中等等一些问题。<br />
