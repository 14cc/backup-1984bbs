他的结论是：<br />
<br />
那么，是不是为了效率，就忽视公平？肯定不行，不公平可以直接导致社会的不稳定，继之，则导致一切对于效率的追求暂时成为泡沫。<br />
<br />
如果有要补充的地方，我想说，郎咸平作为成功的会计师，他在企业具体运作的层面上，在企业财务和收支的层面上，在企业管理者的行为是否正当与合法的问题上，有着基于常识的、正确的见解。在顾雏军问题上，郎咸平的见解尤其正确。</div>

							
							
							
							
