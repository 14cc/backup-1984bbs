先不提违反科学的部分，比如这样的宗教是否能够存在的问题，包括乱伦、裸体的群居生活是否能维持、他们是否可能遵纪守法等。从理论上说，这样的生活是会快速崩溃的，而且持此种观念的人必然视世间普遍道德为渎神而难免有违法行为。<br />
