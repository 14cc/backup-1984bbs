													<h2>当法律遇到人吃人案ZT</h2>
						
						
																											<div id="postmessage_391825" class="t_msgfont">2008年 8月 30日 南方都市报 <br />
　　?? <br />
　　一说到人吃人，人们很容易联想到旧社会人对人的剥削和压迫。但这里要讨论的是，并不是文学修辞，而是同类相食的现实案件。无论旧社会还是新社会，都可能遭遇涉及人吃人的法律案件，那么当法律遇到人吃人案，该怎么办？ <br />
　　1883年，澳大利亚游船木樨草号从英国埃塞克斯前往悉尼，途中沉没，四个幸存者――船长杜德利、助手斯蒂芬、船员布鲁克斯和见习船员帕克――被困在一艘十三英尺长的救生艇上，全部食物只有两个罐头。在第19天，杜德利建议，以抽签的方式选出一个人被杀掉，让其他三人吃掉，以求生存。对此，布鲁克斯反对，斯蒂芬表示犹豫。而杜德利表示：无需犹豫了，帕克身体最弱又没有家人，他肯定先死。杜德利随后杀了帕克，他们三人以帕克的尸体为食撑着。四天后，他们被路过的法国帆船蒙堤祖麻号救起，蒙堤祖麻号进英国法尔茅各斯港短暂停留，杜德利、斯蒂芬和布鲁克斯以涉嫌故意杀人罪被逮捕收监。陪审团同情被告，但为了避免无罪宣告的结果，法官要求陪审团进行特殊裁决，只认定事实。根据陪审团认定的事实，法官宣告被告犯有故意杀人罪，驳回他们的紧急避难抗辩。被告被判处绞刑，随后被维多利亚女王赦免了。 <br />
　　这就是英国历史上著名的“女王诉杜德利与斯蒂芬案”（Her Majesty The Queen v. Tom Dudley and Edwin Stephens）。它牵涉的问题众多，其中每一个问题都争议甚大：杜德利和斯蒂芬该被起诉吗？他们的行为构成紧急避险或正当防卫了吗？他们有罪还是无罪？如果有罪，他们的行为到底构成何种犯罪，是故意杀人罪还是侮辱尸体罪，抑或两者兼有？对他们行政赦免合适吗？这种赦免会构成对法治的伤害吗？当法律规则与道德原则冲突时该如何化解？这个案件牵涉千头万绪，让人左右为难，所以它不断地被人以各种方式解读。 <br />
　　在这个案件和其他类似案件的基础之上，1949年，哈佛大学法学院教授L．L．富勒在《哈佛法学评论》上提出了一个虚拟的人吃人案件，这个名为“洞穴探险”的案例后来被称为“史上最伟大的虚拟案例”：4299年５月上旬，在纽卡斯国，五名洞穴探险人不幸遇到塌方，受困山洞，等待外部的救援。十多日后，他们通过携带的无线电与外界取得联络，得知尚需数日才能获救水尽粮绝；为了生存，大家约定通过投骰子吃掉一人，牺牲一个以救活其余四人。威特摩尔是这一方案的提议人，不过投骰子之前前又收回了意见，其它四人却执意坚持，结果恰好是威特摩尔被选中，在受困的第23天维特摩尔被同伴杀掉吃了。在受困的第32天，剩下四人被救，随后他们以故意杀人罪被起诉，而根据《刑法典》规定：“任何故意剥夺他人生命的人都必须被判处死刑。” <br />
　　“女王诉杜德利与斯蒂芬案”所引发的诸多争议，在“洞穴探险”案中一一再现，后者所蕴含的争议更为庞杂。在纽卡斯国的初审法院，被告被判处死刑。被告上诉到最高法院，富勒虚拟了五位大法官就此案出具的五份不同的判决意见书：首席大法官特鲁派尼从法律实证主义的观点出发，认为法律是法律，道德是道德，同情心不会让法律人违反自己的职业判断，去创造例外，所以他支持有罪判决；福斯特大法官则主张应该根据立法目的，对法律规则进行解释，联邦的法律不适用此案，被告无罪；唐丁大法官认为则这是一个两难的案件，选择回避退出此案；基恩大法官主张法官应当忠于自己的职责，不能滥用目的解释，去规避法律规则的适用，坚持被告有罪；汉迪大法官则主张，抛开法律，用常识判案，通过常识来平衡道德与法律的冲突，坚持被告无罪。在意见上出现了一种奇怪的平衡，最后纽卡斯最高法院决定维持原判。 <br />
　　在阅读这五位大法官的判决意见的过程中，我感觉似乎每个人的论述都有道理，但都又不能完全说服我，似乎这个案件无论如何判决都不能让人信服。无论支持或者反对，我们都可以列举出更多的理由。1998年，美国叶尔汉姆学院哲学系教授彼得&#8226;萨伯，假设此案在五十年后翻案，他再次虚拟了九位大法官的判决意见，具体参见萨伯所著《洞穴奇案：九份新意见》（大陆的译名为《洞穴奇案》，香港商务印书馆的译名为《洞穴奇案的十四种判决》）。 <br />
　　当我在炎热的夏天，通读这十四份判决意见书时，我感到很困惑，它们到底是判决意见书还是辩护律师的意见书？它们彼此之间的观点交锋非常激烈，但都缺少那种一锤定音的力量。这不由让人思考另一个问题：在这个价值多元、诸神纷争的的时代，主张非黑即白的刑事法律审判何以可能？法律又如何化解审判公信力的危机？ <br />
　　??对此，《洞穴奇案：九份新意见》中的大波纳姆大法官认为，“在多元社会中维护和平安宁甚至正义的唯一手段，就是守护法律，我们永远不能为了仅仅由一个派系、教派或者集团所持有的道德或政治观点而置法律于不顾”，因为人民作为整体制定法律来表达意志，在法律之外寻找正义是不正义的。但海伦大法官对此不敢苟同，她指出伯纳姆的观点需要一个前提，“只有当我们想尽办法让所有的观点都能在立法程序中得到倾听”，但建立在这个前提之上的理想的社会是不存在的。立法时并非所有公民的意见都被聆听，因为金钱和特权会干扰选举和立法，多元化声音很难在立法中达成一致，因此人们才需要在法律之外寻求实现正义，比如公民的不服从。 <br />
　　写到这里，我们不妨回望去年中国媒体上发生的“司法职业化vs司民主化”的争论。司法职业化的观点类似波纳姆大法官的观点，而司法民主化的观点则类似海伦大法官的观点。今天看来，这场争论注定难分胜负，它们是对审判公信力危机做出的不同回应，没有高下之别。 <br />
　　笔者以为，重建审判公信力离不开社会共识，而社会共识的重建就需要充分的智识辩驳，洞穴探险案无疑是一个非常好的题目。《洞穴奇案：九份新意见》在香港被市政府作为通识读本推广，可见这本老少咸宜的小册子的特殊魅力。大陆的通识教育已经谈论多年，但少有合适的通识读本，希望这本小册子能够风行起来。 <br />
　　此外，萨伯在书的前言中交代，如果想了解“女王诉杜德利与斯蒂芬案”，不妨参阅布莱恩&#8226;辛普森教授的（A.W.Brain.Simpson）著的《同类相食与普通法》（Cannibalism and the Common Law: The Story of the Tragic Last Voyage of the Mignonette and the Strange Legal Proceedings to Which It Gave Rise,1984）。 辛普森教授的另一本书《法学的邀请》（北大出版社，2008年）已经出版，笔者建议有出版人引进这本《同类相食与普通法》，为我们提供更充分的辩驳材料。</div>

							
							
							
							
