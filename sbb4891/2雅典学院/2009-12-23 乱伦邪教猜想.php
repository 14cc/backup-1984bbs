不应该，应该由人民来决定。如果这种宗教的人在社会上遍行，那么立法肯定不能通过，那么必然有大量“正常人”会选择离开这个国家，其他国家的居民也会采取相应行动通过立法防止这种宗教的传播。之后这个国家会死于什么不能肯定，但是毁灭是必然的。<br />
