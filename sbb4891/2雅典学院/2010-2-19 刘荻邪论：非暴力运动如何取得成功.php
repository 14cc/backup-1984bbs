																											<div id="postmessage_484543" class="t_msgfont">非暴力运动如何取得成功――俄国1905年非暴力抵抗运动随想（刘荻）<br />
<br />
<br />
最近翻译彼得&#8226;阿克曼与杰克&#8226;杜瓦尔合著的《更强大的力量》一书中有关俄国1905年革命的部分，有感想如下：<br />
<br />
1.非暴力运动的手段可能温和，也可能激烈；既可以吃饭、演讲、开会、组织社团，也可以请愿、游行、罢工、挤提银行。只要能够达到目的，温和和激烈的手段都可以使用。<br />
<br />
2. 通过民众运动来改变一个政权往往是一个较为漫长的过程，很少可能通过一场运动就取得最后的胜利。运动如果不能一次成功就必须有所积累。因此非暴力运动需要 能够取得阶段性胜利，能够巩固已经取得的成果，也要具有可持续性。非暴力运动的阶段性胜利包括实现具体的目标和赢得民众的支持。<br />
<br />
3.有人 认为运动遭到镇压就是失败，也有人认为镇压说明了当局的恐惧，因此遭到镇压就是成功。我认为遭到镇压究竟是成功还是失败，要取决于镇压是否有助于实现以上 目标：如果镇压能够唤起民众，为运动赢得更多的支持者，那么遭到镇压就是运动的成功；反之，如果镇压使人们噤若寒蝉，那运动就失败了。<br />
<br />
4.按此标准，厦门PX维权和“08宪章”运动都是成功的：前者实现了具体目标，后者赢得了支持而没有遭到大规模镇压，具有可持续性。相反，如果一场运动在大量成员被捕之后陷入瘫痪，也没有达到唤醒民众的目的，运动就可以说是失败了。<br />
<br />
5. 温和是为了积累，激进是为了突破，突破必须以积累为基础。成功的突破能够为下一阶段的运动积累更多的资源，失败的突破也会消耗掉已经积累起来的资源。反对 运动在自身的实力还不够强大的情况下，应该以积累为主，突破为辅。亚当&#8226;米奇尼克提出的“新演进”和“建设我们自己”的主张，就是要为今后的突破进行积 累。“08宪章”运动也应该走“新演进”的道路。<br />
<br />
6.不把为专制政权服务的个人当作敌人是一种政治智慧，因为无论是暴力革命还是非暴力运动都要寄希望于警察、军人和公务员的倒戈，非暴力运动可能还会希望得到体制内开明派的支持，如果一开始就把他们当作敌人，这个目标就更难实现。<br />
<br />
7.不把政权本身当作敌人也是一种政治智慧，因为运动既然不可能一次取得成功，那我们就必须接受对手做出的较小让步，而不是一律将其视为敌人的诡计而加以拒绝，让“敌人意识”成为取得最终胜利的敌人。<br />
<br />
8. 在1905年的俄国，布尔什维克曾经发传单呼吁工人不要参加沙邦神父手无寸铁前往冬宫向沙皇“卑微忠贞请愿”的游行，他们认为：“自由是用鲜血换来的，自 由是用武器在激战中赢得的。”但具有讽刺意义的是，沙邦神父的请愿遭到镇压，成为了1905年非暴力抗争的开端；沙邦神父高喊：“和平手段已经失败！”之 时，为期一年之久的和平抗争才刚刚开始；而年底布尔什维克等革命派的武装起义遭到镇压，则是这场运动失败的标志。</div>

							
							
							
							
