西藏新疆动乱之源在胡耀邦？<br />
<br />
<br />
&nbsp;&nbsp;80年12月胡耀邦、万里到西藏，闯了一个乱子。当时他们了解到中央补贴给西藏的钱每年5、6亿元，80％用于在西藏的汉族干部，包括在西藏进行工业交通等各种事业建设的汉族职工。这本来是很自然的，要搞工厂、修路只能用这个钱啊，从事工厂生产和修路建设的汉族工人工资也只能用这个钱啊，但胡却错误地作出结论：中央一年给的5、6亿元钱，80％用在西藏的汉族干部。为了使中央的补助真正用于藏族人，要把80％的汉人从西藏撤出来。另外，本来阴法唐在那里工作得很好、很深入，工作中或许会有点&quot;左&quot;的东西。胡就抓住这点进行批判，同时，阴法唐在搞土改时从农奴中培养起来、提拔起来的藏族积极分子和干部也一起挨批，使得这些积极分子抬不起头来。<br />
&nbsp; &nbsp; 后来赵紫阳派伍精华去西藏，伍是彝族人，出身很好，但接受了胡、赵的影响，去了西藏后，专走上层路线，和广大的藏族干部都不沾边，影响很不好。直至胡锦涛去了之后，原藏族积极分子受压的状况才得到纠正。当时在全国影响最大的是80％的汉族干部要从西藏撤出。这个消息一传到新疆、内蒙....跟着就发生了类似问题。特别是新疆，那时的书记是汪锋。 1980年时，搞防苏备，汪对战争形势估计得很严重，惊慌失措。在伊犁边界，要很多汉族干部往东撤，县级以上干部都要回内地。有些汉族干部乐得回去，而少数民族干部中有民族主义思想的乘机鼓噪要挤走汉族干部，成为一种风潮，而江锋同志又只是依靠少数几个人，既得罪了汉族干部，又得罪了少数民族干部，在新疆闹得不可开交。到了1980年，新疆的区<br />
党委扩大会议都开不下去了。<br />
&nbsp; &nbsp;没办法了，胡耀邦就把他们找到北京开会，搞了个纪要。其倾向是压汉族干部、抬民族干部。这个纪要一传达，双方的矛盾更加激化，会也开不下去了，他们来电报向中央请怎么办。不知赵和胡是否商量过，赵找到我说：新疆问题，搞得这么僵，他们来了个，要中央给他们回电。于是，赵叫我起草了一个复电。这里比较公道地讲了一下汉族干部、解放军所起的作用，还讲到新疆党组织成立以后汉族干部所起的作用，强调民族干部和汉族干部要互相尊重、互相团结。这个电报到了那里，反应各异，民族干部比较冷淡。这样就形成所谓民族干部要“纪要”，汉族干部要“电报”。问题解决不了，后来要我负责起草一个新疆问题的决定。当时用了比较多的时间来搞这个东西，但后来太长，实际上没用上。<br />
&nbsp; &nbsp; 这个时候，胡耀邦感觉到在民族问题上出了毛病，要收拾这个局面，于是提出“两个离不开”。在酝酿过程中，我听说(没看到文字)，原来的纪要上他讲过在新疆问题上，党中央只要三个权：国防权、外交权、内政否决权。即除了国防、外交归中央管外，新疆自治区范围里的内政都由你们决定。决定了以后，中央只有这一点权，即看到有哪些不妥当，可行使否决权，其他事情都由你们决定。我就问胡，在民族地区怎么能够做出这样的决定呢？除了三个权，其他都由民族自治区自己管，这怎么行呢？他和我说，这不是他说的，这话是万里说的。<br />
&nbsp; &nbsp;事情后遗症太大了，很长时间都没有安定。后来新疆又出了一件事：我们要提铁木耳，新疆要提司马义；中央采取了一个措施，即把司马义调到民委当主任，当时乌鲁木齐有一、两千人(民族人员)上街拥护司马义。在这种情况下，王震出了个主意：汪锋不能再呆下去了，要调出，把王恩茂调回去，又调谷景生去当区党委第二书记。从此之后，新疆的局势才逐步稳定下来。<br />
