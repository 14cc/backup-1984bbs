													<h2>宋庆龄晚年说：“不勉强吧!”</h2>
						
						
																											<div id="postmessage_492499" class="t_msgfont">[转贴]宋庆龄在文革中给毛主席的七封信<br />
<br />
文章提交者：白衣大褂 <br />
<br />
1994年11月，书记处审查通过了党史编委会整理的关于宋庆龄建国后32年经历的材料。<br />
<br />
邓小平批示：还是要去伪存真，敢于修正不实之处。<br />
<br />
陈云批示：宋庆龄的品质高贵，在于信仰始终如一， 爱国、爱民、爱和平。不做违心事，不讲违心话。要尊重她这一点。<br />
<br />
材料内容提要：<br />
<br />
宋1950年3月给党中央写信要求入党，52年10月再次给毛泽东写信提出要求。毛对宋说：“你在政治上完全够格，在革命战争的岁月，实际已是党的优秀领导者之一。今后还有许多工作需要你做。有些事我们做不好，你做合适。我在党内说了：党的高级干部还要向你学习，学习你的革命坚定性，对新中国革命的贡献，你的作用比李济深、沈老还要大。所以还是留在党外好。”<br />
<br />
1955年11月，宋给毛写信：“我很不理解提出对工商业的改造，共产党曾向工商界许下长期共存、保护工商业者利益的诺言。这样一来，不是变成自食其言了吗？资本家已经对共产党的政策产生了怀疑和恐惧，不少人后悔和抱怨。”毛批示：“宋副委员长有意见，要代表资本家讲话。”<br />
<br />
1957年宋又写信给党中央：“党中央号召大鸣大放，怎么又收了？共产党不怕国民党八百万大军，不怕美帝国主义，怎么会担心人民推翻党的领导和人民政府？共产党要敢于接受各界人士的批评，批评人士大多是爱国爱党的，一些民主党派人士为新中国的解放，作出了家庭、个人名利的牺牲， 一些二、三十岁的青年知识分子怎么可能一天就变成反党反社会主义分子？我很不理解这个运动，我想了两个多月，还是想不通，有这么多党内党外纯粹的人会站在共产党和人民政府的对立面？要推翻共产党？”<br />
<br />
从1958年起，宋曾推病拒绝参加人大常委会。党中央委派刘少奇、周恩来、董必武去做工作， 宋只得继续参加。<br />
<br />
1959年4月，宋在人大被推举为国家副主席。宋先后两次推辞：“我是落伍了，思想跟不上，才挂个名，作个样子，对国家不利。”提议由李富春或乌兰夫担任。<br />
<br />
宋任国家副主席，是刘少奇、董必武、林伯渠、李富春提议的，政治局讨论时，21人中18人赞成，3人反对，反对者是：毛、林彪、康生。<br />
<br />
当时毛发言：“宋是我们民主革命时期的同路人， 在社会主义革命时期，她和我们就走不到一起了。从不赞成我们的方针路线到反对我们的方针路线。我们同她是不同的阶级。”<br />
<br />
文革期间，宋先后给毛和党中央写了七封信，表达了她对“文革”的不理解、反感，并对共产党极度失望。67年8月、69 年11月、76 年6月，宋曾三次产生厌世思想， 在信中以及对来探望她的领导人的谈话中流露出对自己所选择的道路感到怅惘和说不出的苦闷。七封信中说：“我不懂文化，说小说都是政治，而且都是毒草，我糊涂了，一夜天下来，一些和我一起工作的同事都变成了走资派、反党集团、野心家、牛鬼蛇神。中央要我学习批判揭发刘少奇，我不会作的，刘少奇主席在党中央工作了三四十年，今天会是叛徒、内奸！ 我不相信，一个叛徒内奸当了七年的国家主席，现在宪法还有效吗？怎么可以乱抓人、乱斗人、逼死人？党中央要出来讲话。这种无法无天的情况，自己伤害自己的同志、人民，是罪行。我们的优秀干部从与国民党的战斗中走过来，却死在自己的队伍中，这是什么原因？”<br />
<br />
1970年3月，毛对周恩来说：“她不愿意看到今天的变化，可以到海峡对岸，可以去香港、去外国，我不挽留。”并指示周恩来、李先念把他的话传达给宋。传达时他们说：“主席很关心你，知道你的心情不怎么好，建议你到外面散散心，休息休息。”宋说：“是否嫌我还在？我的一生还是要在这块土地上，走完最后几步。”于是宋推病拒绝出席一些节日活动和招待会，说“我参加会伤感，还是不参加，参加一次，回来就要进医院。另外，我也不想做政治上的点缀。”<br />
<br />
1980年11月，宋给党中央写了她一生中最后的一封信：“一、国家要振兴，恢复元气，这是一次大好时机，二、要总结建国以来政治运动对国家对人民造成的创伤， 三、请不要把我和国父放在一起，我不够格的。”1981年5月，胡耀邦、李先念到医院转告宋，政治局决定接受她为正式党员，宋听后微笑说：“不勉强吧！31年了，我的心冷了，人生的路将要走完了。”<br />
<br />
胡、李问宋还有什么要求，她提了两点：“我死后还是回到上海安息；我有些储蓄，办个福利基金。”<br />
<br />
宋逝世后，中共违背宋的意愿，追认其为中共党员。<br />
<br />
注：这是何方老提供的党史材料，原来的题目是：《宋庆龄晚年说：“不勉强吧!”》。其实这是宋庆龄最后的态度。而她以前的许多话，都是重要的表态，所以改为这个题目。<br />
<br />
何方老，革命前辈、党史学者，著有：《党史笔记――从遵义会议到延安整风》上下册，《从延安一路走来的反思》上下册。</div>

							
							
							
							
