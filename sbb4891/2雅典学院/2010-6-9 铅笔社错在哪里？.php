																											<div id="postmessage_669280" class="t_msgfont">　　契约的当事双方事实上都有违背契约的可能性。<br />
　　可以假定，违背契约者认为他违背契约比继续履行契约获得的收益大。<br />
　　对契约的违背行为，应该由当事双方约定各种可以设想的情形下，违背契约一方对遵守契约一方的补偿。<br />
　　但是，如果法律规定，以某种形式的违背契约，比如不可抗力，比如罢工，将不受违约补偿条款的限制――那么，这种情况下的违背契约就是一种义务免除基础上的行为权利。<br />
　　在此基础上，工人有谈判的权利――结果是：（1）双方协商一致，更改原有契约；（2）双方协商不成，一方违约，向另一方赔偿，或者被起诉要求赔偿；（3）一方妥协，继续履行原有约定。<br />
　　在此基础上，工人有罢工的权利――结果是：（1）罢工受到法律的保护，属合法行为，但是结果不确定；（2）罢工属非法行为，遭到刑事处罚，并遭到民事赔偿的起诉；（3）法律无此方面规定，双方或者其中一方最终必将会妥协――请不要误解妥协的意思，在此不多解释。</div>

							
							
							
							
