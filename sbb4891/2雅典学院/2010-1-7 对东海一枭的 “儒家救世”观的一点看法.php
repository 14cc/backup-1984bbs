													<h2>对东海一枭的 “儒家救世”观的一点看法</h2>
						
						
																											<div id="postmessage_418440" class="t_msgfont">最近不知是不是84里面的儒教分子杨恒，刘松萝们顶不住了，一位老牌儒教分子“东海一枭(ID:我心忧伤）“又冒了出来，大肆鼓吹他的“儒家救世”观。<br />
我认为，儒教救世的观点是十分荒谬的。主流儒教的价值观体系是建立在对人“性本善”天性认识基础之上的。然而，现代心理学已经证明，人因为天性自私，所以更容易做坏事，所以，人性倾向于恶。一个基于错误人性观基础上的思想学说，会主张建立一个什么样的社会秩序呢？<br />
暴政！因为暴政来源于专制，性本善理论却容易成为专制的理论基础。“性本善”论必然导致追求圣人治国的价值取向，进而走上追求圣王的道路，再进而衍生、强化、维系一种人治――专制独裁的政治体制。<br />
中国两千多年来始终是在专制独裁的泥潭中无法自拔，原因正是如此。儒教为了自己的思想统治地位，一方面将秦史歪曲成因“焚书坑儒”而覆亡（事实上，秦朝的灭亡，是由于法家太过迷信严刑峻法的作用而走上“暴力治国”的邪路，忽视了对人民思想的教导导致的），制造“治国非儒不可”的错误舆论来蒙骗世人，另一方面，利用儒教的“圣王”论，为专制统治构建理论基础；专制君主则给予儒教以垄断地位的专制意识形态（以至于出现了大字不识一箩筐的元朝皇帝加封孔子为“大成至圣文宣王”的荒诞一幕。<br />
由于两千多年的思想意识垄断地位，儒教在中国根深蒂固，儒教死抱不放的’圣王“论价值取向及追求大一统人治的政治理念，成为近代以来中国建设民主宪政的最巨大的思想障碍。蔡元培、鲁讯、陈独秀等五四先贤们正式因为看到了儒教对中国进步之路的阻碍，才奋然而起批判儒教，意图走出将中国拯救出专制独裁泥潭的新道路。<br />
某些儒教分子对五四的批判毫无道理，儒教真的好，为什么会有这么多的知识分子，青年才俊走上了”砸烂孔家店“的道路，为什么砸烂“孔家店”在广大青年和知识分子中那样得人心？不正是因为这些人亲身感受到了儒教思想的专横、野蛮、压抑和束缚吗？有些儒教分子以日本、韩国、新加坡的例子来“证明”儒教与民主思想可以并行不悖，事实上，这些国家的儒家已经处不可逆转的衰落当中，只是衰落速度比较缓慢而已，日本宅男阶层的壮大，韩国社会热衷于群众运动的现象，昭示这这一无法否认的现实。<br />
当然，五四不是没有缺陷。事实上，五四运动确实给不久之后的共产革命运动铺平了道路。陈独秀等人砸烂了“孔家店”，却由于当时掌握政权的人很大一部分是儒教分子（直到40年代末期，这种现象依然十分严重，甚至46年宪法的起草者竟然也是个支持独裁的儒教分子），致使他们没有政权的支持，没有强有力的后盾来协助他们重建新的价值观体系，导致“打倒孔家店’后中国陷入了价值观迷茫状态，这时候趁虚而入的苏俄苏维埃邪教送来了前景十分“美好”的共产乌托邦价值观，陷入价值观迷茫状态的中国人，为了摆脱贫穷落后的现状而不顾一切的将这个荒谬的邪说奉为至宝，等他们醒悟过来的时候，一切都晚了。<br />
这个悲惨的事实，恰好从反面告诉我们一个强有力政权用政治力量对社会价值观进行改造重建的意义。事实上，邪恶的共产思想至今在中国也有着大批信徒，造成这一切的原因与政权运用政治力量对社会价值观的改造是分不开的。<br />
儒教并不能救世，儒教只会给中国带来独裁。随着共产主义乌托邦的破产，中共早已无法维系社会整体的共产主义价值观，中共已经开始重走回“儒教治国”老路，利用政治力量大力推行儒教价值观，借此维系其专制统治。<br />
东海一枭此时重提迎合中共的“儒教救世”论，不得不让人怀疑其居心。</div>

							
							
							
							
