													<h2>美国的韧劲</h2>
						
						
																											<div id="postmessage_568399" class="t_msgfont">人渣经济笔记 &#187;<br />
<br />
写好很长时间了，捂着，今天决定发了。<br />
<br />
很多人觉得美国衰弱了，永久的衰弱了。美国国会的僵局让你这个觉得这个国家做什么都只能是缓慢改进，进一尺，退八分，就更不要说经济危机的废墟还远没有清理干净，经济的稳健复苏还远未确定。从很大程度上，美国是衰弱了，可能是永久的，不太可能再重新回到二战结束之后或者苏联垮台之后的支配地位，特别是在经济上。<br />
<br />
我对经济史一知半解，但我想从一个很窄的角度说说美国的韧性，好让我们把视野稍微放得长远一点。<br />
<br />
从某种意义上，美国在历史上的崛起是悄无声息的。悄无声息的意思是和今日的中国的相比。我做了一点简单的计算，从1820年到1870年，美国平均的GDP增长率大概是4.3%，但其中一部分是因为人口的快速增长导致的，在这50年间，美国的人口平均增长率超过了2％，所以如果没有人口增长，美国的GDP增长率也就是2％左右。从1870年到1929年，也就是大萧条开始之前，美国增长的极值是一年13.8%，但平均起来这60年的增长率也就是3.8%，剔除人口增长的因素，也就是2％左右。二战以后的60多年，美国增长的极值是8.7％，平均只有2.9%，去掉人口因素，还是2％左右。我说的这些当然已经是经济史和经济增长文献中非常广为人知的基本事实。美国用剔除人口因素之后的2％的速度增长了将近快200年，但是一不小心，就把所有的国家都遥遥的抛在后面。如果说美国是一个经济奇迹，那就是这个国家在最近两三百年比世界上其它的哪个国家都更有韧性，机缘巧合也好，地理位置也好，制度决定也好，强多豪取也好，不管你喜欢什么解释，小步慢跑的美国在很多方面都达到了人类历史上所有其它国家都尚未达到的高度。<br />
<br />
中国已经大步快跑了30年，乐观的人觉得至少还能再这样跑30年。如果真是如此，那个时候，中国会是经济总量第一大国，人均的水平也会相当可观，大概能达到当前发达国家的水平吧，也就是中国还要这样快跑30年才能在人均意义达到今天的发达国家水平。但即便看好中国的人，大概也会承认，上面说的这是最好的情形，政治的，经济的，军事的，环境的，资源的，人口的很多因素，都有可能让最好的情形无法出现。如果要看100年或者200年，大概完全没有人知道会怎样。用术语讲，就是即便对中国的未来点估计比较高的人，也会承认，这个点估计的置信区间是很宽的。中国的历史上，应该还没有出现过持续100-200年的盛世（所谓康雍乾盛世总共加在一起不到140年，前面在打仗，后面在衰落，中间真正的盛世，能有100年就不错了，而且也只是恢复性增长和相对的稳定，并没有真正意义上的现代经济增长）。而美国则不同，即便把南北战争考虑进来，除了偶尔的中断外，这个国家温和的盛世已经持续了至少有200年了，这个国家虽然也面对很多的挑战，但历史证明这个国家有着一种很强的韧性，有很强的自我纠错能力，保持了平和的进步和中国很多人蒙昧以求的稳定。<br />
<br />
这种韧性不是偶然的。中国如果无法拥有这种韧性，那很多人的沾沾自喜恐怕还太早了。似乎至少得有100年到200年，才能有把握的说，那是盛世，而不是昙花一现。<br />
