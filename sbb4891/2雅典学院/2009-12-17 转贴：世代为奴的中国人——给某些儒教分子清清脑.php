													<h2>转贴：世代为奴的中国人――给某些儒教分子清清脑</h2>
						
						
																											<div id="postmessage_379594" class="t_msgfont">身为中国人最大的悲哀，就是难以摆脱的世代为奴的宿命，就算是身体脱离了中国的土地，精神上也仍然无法从终生为奴的潜意识中解脱出来。 <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp; 我想，中国要实现现代化，民主化，就是要破除“炎黄子孙”“中华民族”“五千年文明”“大一统”之类的迷思。因为这些东西，不仅不符合历史真实，而且只是统治者用来证明自己统治合法性的工具。 <br />
<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp; 中国从古到今的政治观都是“家天下”的政治观，皇权所及之处都是皇帝的私产，皇国的子民实质上都成为皇家的家奴，没有西方公民社会的概念，发展不出自由，民主，人权这些现代理念，中国人的脑袋里产生不了这些东西，就是因为被所谓“炎黄子孙”“大一统”之类的思想桎梏限制住了。 <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp; 而这也正是统治者希望达到的效果。以我们都是同一祖先的名义，以我们都是同一血脉的借口，统治者就有了奴役“同胞”的天然合法性，而我们则有了天生为奴的义务，而且我们还有彼此监督对方为奴的义务！ <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp; “炎黄子孙”是一个谎言，是直接用以贯彻“家天下”政治观的历史法理依据。现在的“党天下”也仍然用这一套来作为证明自己统治合法性和加强统治稳定性的工具，“党天下”本质上也仍然是“家天下”政治观的延续。 <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp; 我已经说过了，黄帝，炎帝，尧，舜，禹等等“人物”，不过是春秋战国甚至晚至秦汉时代的方士文人根据神话传说虚构出来的，用以证明中央集权的专制制度，“大一统”等等政治合法性的工具，不幸却愚弄蒙蔽了世人两千多年。此问题可参考《所谓五千年文明古国的无稽之谈》《华夏文明的雅利安来源》等文章。 <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp; 祖先崇拜是一件很荒谬的事情，只需要作一个简单的数学运算就能够明白，每个人往上推一代都会有两个直系父代，往上推两代就会有四个直系父代，以三十年为一代，则往前推三千年，也就是一百代之前，你有二的一百次方个数量的“祖先”，当然这是理想情况，即使扣除重复交叉的情况，也仍然可以得出结论：那时世界上的每一个人都是你的“祖先”，也可以说都不是你的“祖先”，因为作为单个的个体他的血统已经在你的身上稀释到接近于零，而奉整体的人群为祖先也是说不通的，因为历史上分分和和，打打杀杀，个体与个体之间的利益都是矛盾的，冲突的。 <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp; 所以说，“祖宗”这个概念，只有在与你有直接联系和接触的父母辈和祖父母辈那里才有意义。最多可以上溯到曾祖父母辈。 <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp; 至于“亵渎祖先”“咒骂祖先”之类的责难，则是那样的苍白无力，首先，对历史上某些人的批评和不敬不等于就是“亵渎祖先”“咒骂祖先”，其次，“祖先”也没有什么不可以亵渎，不可以咒骂的。前面说了，那些人不过是历史的长河中曾经存在过的一些跟你我并无绝对逻辑关系的生命个体而已，一堆跟你我并无本质区别的蛋白质活动物体而已，我也看不出有什么理由一定要对千百年前那些野蛮残忍而又愚昧（至少不比我们更文明）的家伙给予比我们身边之人更多的尊重和爱戴。 <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp; 何况，当中国人把自以为的“祖先”－－北京猿人，元谋人等的头骨，牙齿等遗骸拿在手里敲敲打打，细细玩赏，放在玻璃柜里供人指指点点的时候，我看不出他们对“祖先”有什么尊重态度，何况他们还固执地硬要把这些早已经被我们真正的“祖先”当作晚饭吃掉了的已经灭绝了的未进化完全的类人猿认作是自己的“祖先”，我看不出这对我们真正的“祖先”有什么尊重的态度。 <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp; “中国”“中华民族”等图腾也不过是一个短命的概念，民国之前并无“中华民族”的概念，也没有现在意义上的“中国”概念（虽然先秦时已有“中国”一词，但不过是完全不同的另外意义），不仅“中华民族”，就是“汉族”，本身也是一个伪概念。只不过因为定势思维的惯性，我们也就机械地接受了它的合法性和伪真实性。就是将近一百年之后的现在，这些概念的稳定性仍然是不确定的，随时有土崩瓦解的可能性。我们抛弃它不应该有任何心理负担。 <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp; 至于“文化”的问题，张口闭口就是“中国文化”如何如何的人们，我看“文化”在他们嘴里和大脑中的全部内容也就是“文化”这两个字而已，他们真正明白“中国文化”的真实内涵和本质是什么吗？ <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp; 我认为，剔除掉附着在“中国文化”表皮上的那些装饰用的漂亮羽毛之后，中国文化最本质的内容，就是中国人的生活方式，思维方式，一种最腐朽，最堕落的生活方式，太监的生活方式，奴才的生活方式；一种最愚昧，最阴暗的思维方式，就是奴才的思维方式，太监的思维方式，就是太监文化，奴才文化，太监哲学，奴才哲学。 <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp; 而遗老遗少们想要复兴的古代“文化”，不过是已经僵死的太监文化的锦袍上偶尔飘落的一缕发光的丝毛，他们把这个作为支撑自己精神持续亢奋的图腾，意图搞牛头不对马嘴的“复古”活动（应该是伪古活动），只是为奴自虐症发作的晚期症状而已。 <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp; 对于历史和传统的认识，我可以三句话概括：民族本非一体，地域本非一统，文化更非一脉单传。 <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp; 我看历史虚无主义，民族虚无主义没有什么不好，其一，它符合历史的真实，其二，它符合人性的需求。 <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp; 中国人，对诸如此类的很多无现实意义的问题都不能“虚无”地看待，但是对不论是作为个人还是群体都最不应该抱“虚无”态度的人权，民主，自由等等理念却那么的麻木不仁。 <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp; 中国人都患有中国病－－心理上为奴自虐的强迫症，不仅潜意识里自认有对统治者为奴的义务，而且奴民们彼此也默认有彼此为奴的义务，彼此监督对方为奴的义务，而历史虚无主义，民族虚无主义，就是医治“中国病”的最好药方。 <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp; 身为背负三千年为奴自虐的精神上原罪的中国人，要摆脱世代为奴的宿命，就没有什么不可以抛弃的，应该深刻地认识到身为中国人的耻辱，身为潜意识里为奴自虐的奴民的耻辱，身为中国人就是耻辱，就是原罪，知耻的人才有得拯救的机会，要做到彻底摆脱世代为奴的精神负担，就是要从灵魂最深处进行一场最彻底的“去中国化”的思想运动。 <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp; 凤凰浴火，才能重生。<br />
