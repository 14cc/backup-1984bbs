													<h2>余杰：齐奥塞斯库的幽灵在中国徘徊</h2>
						
						
																											<div id="postmessage_344467" class="t_msgfont">*中国社会可能“揭竿而起”<br />
<br />
由于胡锦涛当局疯狂打压《08宪章》、关闭以公盟为代表的NGO组织、审判谭作人等调查四川地震死亡人数的志愿者，将所有温和、理性、渐进、改良、倡导宽容和解的公民当作非要置之于死地的敌人，使得中国社会的转型、以捷克“天鹅绒革命”的方式发生的可能性变得越来越小，而以罗马尼亚“揭竿而起”的方式发生的可能性变得越来越大。中共执政六十年后，一个幽灵――齐奥塞斯库的幽灵，在中国徘徊。<br />
<br />
<br />
*罗共败亡始于群体性事件<br />
<br />
齐奥塞斯库和罗马尼亚共产党的败亡，始于一桩“群体性”事件――这样的“群体性事件”几乎每天都有在当下的中国发生，只是尚未形成燎原之势而已。在罗马尼亚与匈牙利边境的小镇狄米索拉，有一位名叫托克斯的神父，一向倡导人权。1989年8月当局派警察包围托克斯神父的住宅，切断其饮水和食物的供应，民众携带食物上门救助亦遭到拦阻。11月，有4名蒙面暴徒闯入托克斯家中，殴打托克斯及其怀孕的妻子，并以刀刺伤2人。此事件近似于近期中共当局频频派遣黑社会打手或由秘密警察装扮成流氓，殴打李和平、孙文广、艾未未等人权律师和异议知识分子，两个独裁政权的做法如出一辙。托克斯神父在当地享有崇高之威望，当地民众对此愤怒之至，乃捣毁公共场合悬挂的齐奥塞斯库的肖像，并上街示威游行。安全警察奉命镇压，向游行队伍前排的儿童开枪射击。12月20日，群众不畏生命危险，上街抗议增加到5万人。罗共中央命令军队参与镇压，当地驻军抗命不从。安全警察遂开赴前线，不仅枪杀若干拒绝开枪的官兵，而且大肆杀戮手无寸铁的民众，是日伤亡人数高达数千。目击的外国记者将其比作半年前北京天安门广场的屠杀。中共在“六四”屠杀之后的20年间，在四川汉源、广州太石村、浙江东洲以及西藏、新疆等地，亦多次使用独立于军队之外的武警镇压民众之抗议，开枪已经成为独裁者家常便饭的统治模式。<br />
<br />
狄米索拉事变发生之后，齐奥塞斯库提前结束在国外的访问，在布加勒斯特举行群众大会，希望藉此展示其统治之稳固及人民之拥护。长期以来，齐奥塞斯库个人专断和大搞个人迷信。他们夫妇的行为，几乎成了罗马尼亚人民的惟一新闻来源，到处是其著作、报告、照片、甚至耗费重金到国外出版齐奥塞斯库选集，营造其受到世界人民欢迎的假象。官方文件在指称齐奥塞斯库夫妇时，以大体的“他”和“她”出现，宛若圣经中对上帝的指称。罗马尼亚全国到处都有“齐奥塞斯库万岁”的标语和塑像，博物馆内有其专馆。齐奥塞斯库每到一地，官方都要组织成千上万的群众欢迎，群众必须提前几个小时到场，即使风吹雨淋、烈日暴晒也岿然不动。官方还组织一批政治“啦啦队”，坐在会场前头七八排，齐奥塞斯库讲话时，每隔两三分钟都站起来鼓掌、叫好。他每讲一次话，听众都得起立鼓掌、欢呼几十次。这一次的群众大会便是按照传统模式精心组织的，参与者都是经过严格政治审查的党员和积极分子。在中国，自毛泽东以下的历届党魁，都喜欢组织见百万红卫兵，到邓小平、江泽民和胡锦涛都乐此不疲地举办国庆阅兵，均是如此。<br />
<br />
就在齐奥塞斯库开始演讲不久，人群出现了骚动，人们对其讲话报之以嘘声，并高喊“打倒齐奥塞斯库”。一向养尊处优、自以为是的齐奥塞斯库顿时神色愕然，手足无措，急忙退出会场，避入罗共中央党部大厦。同时，他命令军警立即出动，以坦克驱散群情激昂的民众。次日凌晨，15万民众上街示威，与安全警察形成对峙。国防部长因拒绝下令镇压而被齐奥塞斯库秘密处决。于是，总参谋长和其他高级将领忍无可忍，率领陆军倒向民众一边，合力将安全警察击溃。当日晚间，“救国阵线”政府宣布成立，并发布了对齐奥塞斯库及其党羽的搜捕令。<br />
<br />
<br />
*胡锦涛应警惕齐奥塞斯库下场<br />
<br />
齐奥塞斯库见大势已去，率领妻子及少数亲信卫士，乘坐直升机从中央党部大厦楼顶逃走。为了躲避雷达的追踪，他在中途下令飞机降落，并挟持一辆小汽车继续逃亡。在逃亡路上，这名七十一岁的独裁者感到无力回天，多次伏在汽车仪表键盘上哭泣。当天晚上，这辆汽车被群众拦下，人们将齐奥塞斯库夫妇转交给军方处置。三天之后，新政府组织特别军事法庭，以集体屠杀、破坏国家经济、盗取国家财产等罪名判处其死刑并立即执行。当死刑判决下达之后，齐奥塞斯库的妻子和副手、一直倨傲不驯的埃列娜，终于精神崩溃，向行刑的士兵喊道：“你们怎能向我们开枪，我曾经那么关怀你们，我是你们的母亲。”一名士兵断然回答说：“不，你不是我们的母亲，你是杀死我们母亲的凶手。”话音刚落，枪声骤然响起。在12月26日电视台播放的画面中，一代枭雄已经卧倒在一片血泊之中，与当年的墨索里尼是一样的下场。这一幕，会在中国重演吗？当胡锦涛一意孤行地使用谎言和暴力维持社会稳定的时候，有没有想过，这样做其实是在饮鸠止渴？<br />
<br />
当年，齐奥塞斯库对罗马尼亚所有的书刊、报纸、信件一律实行检查制度，他甚至颁布了《大罗马尼亚打字机法》，出租或借用打字机为非法，谁想拥有打字机，必须取得警察的批准。而当局的宣传机器开足马力制造谎言、愚弄百姓。如今，胡锦涛即便想以齐奥塞斯库为师，亦难以奏效，因为现在已经进入互联网时代了。任何想在网络上修筑长城的做法都难以实现，互联网的本质就是信息的自由流通。会使用“翻墙”软件突破网络封锁的中国网民越来越多，网络民意正在迅速成长，当局的谎言不仅无法欺骗大多数民众，而且沦落到宣传者自己也完全不信的地步。<br />
<br />
当年，齐奥塞斯库对老百姓实行严酷的警察统治，罗马尼亚的人口仅二千多万，安全警察便多达七十六万，实际上这是齐奥塞斯库一个人的“御林军”。这些秘密警察拥有特权，肆无忌惮地践踏法治。知识分子和民众如果批评政府，结果便是失业、软禁或投入精神病院，与真正的精神病患者关在一起，直到无人能区分他们。但是，表面上看无比温顺的羔羊，当长期郁结的愤怒突然爆发，将如火如荼，胜过暴风骤雨。秘密警察无论如何残暴，总不能将人民全部屠杀，只剩下齐奥塞斯库当没有被统治者的光杆独裁者吧？<br />
<br />
<br />
*历史留给胡锦涛的机会<br />
<br />
今日中国的情势也是如此，可以说，举国上下，没有很多人从心底里尊重和爱戴胡锦涛，大家最多是无可奈何地“忍受”他罢了。胡锦涛对此一清二楚，他不再到处悬挂自己的画像和大量出版自己的文集，转而集中资源扩大武警和政治警察的编制与势力。在胡锦涛时代，此两种力量膨胀到空前的规模。尤其是国安、国保这两支专政队伍，表面上替中央盯住“不安定的因素”，却极大地破坏了宪法的权威与政府的合法性。须知，当民众的忍耐转化为反抗的临界点到来之时，便是如同《诗经》中所说“时日曷丧，予及汝偕亡！”再毒的太阳也有落山的时候，当民不畏死之时，再恐怖的暴力都无法奴役他们了。那时，胡锦涛重蹈的，将会是齐奥塞斯库当年的逃亡之路。<br />
<br />
齐奥塞斯库统治罗马尼亚长达二十五年之久，与中共历届党魁相比，仅次于毛泽东而长于邓小平、江泽民和胡锦涛。齐奥塞斯库掌权时，保安极其严密，且留下诸多“后路”――在政府大厦、中央党部大厦和其卧室之间，均有宽敞的地道联接，并有荷枪实弹的安全警察日夜守卫，以供紧急事变时逃亡之用。但机关算尽，齐奥塞斯库夫妇仍未能避免死于非命的结局。据说，齐奥塞斯库曾经设想到中国来寻求政治避难并安度晚年，只是事发仓促，他已经来不及调度可以长途飞行的专机了。如果中国发生类似事件，胡锦涛和政治局的同僚们会选择逃亡到哪里去呢？虽然他们的子女和孙子孙女大都生活在欧美国家，但他们本人是不敢去的，那就只剩下北韩、缅甸、老挝、古巴、苏丹这些难兄难弟了。但中共若崩溃，这些寄生虫政权还能够幸存下来吗？偌大的地球，他们在哪里寻找到一处安全的藏身之地？我想忠告胡锦涛们：与其日后仓惶逃窜，不如今日践行《08宪章》。这是历史留给胡锦涛的机会，惟一的可以为国家、民族和人民作出贡献的机会。<br />
<br />
<br />
《争鸣》2009年第10期</div>

							
							
							
							
