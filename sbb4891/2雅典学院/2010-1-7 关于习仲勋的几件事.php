													<h2>关于习仲勋的几件事</h2>
						
						
																											<div id="postmessage_419590" class="t_msgfont">作者：朱池 <br />
习仲勋，陕西富平县人。1926年5月在县立诚中学高小读书时加入中国共产主义青年团。1928年4月转为中国共产党党员。后从事农民运动。1930年1月被派往杨虎城部警备骑兵第三旅开展兵运工作。1932年3月在甘肃两当发动兵变，失败后曾转赴渭北、三原开展革命工作。1933年3月起任陕甘边游击队总指挥部政委、中共陕甘边特委军委书记、陕甘边革命委员会副主席，参与创建以照金为中心的陕甘边革命根据地、以南梁为中心的陕甘边革命根据地。1934年2月起任陕甘边革命委员会主席，中共陕甘边特委代理书记、军委书记，陕甘边苏维埃政府主席。<br />
<br />
　　1935年9月习仲勋在肃反中与刘志丹、高岗等人一起被关押，差点被活埋！红一方面军长征到达陕北后，在中共中央直接过问下获释。<br />
<br />
　　1936年1月任中共关中特委常委、苏维埃政府副主席。同年6月参加西征，曾任中共环县县委书记。9月调回关中任中共特委书记、游击队政委。<br />
<br />
　　抗日战争爆发后，任中共关中地委书记、专员公署专员、军分区和关中警备区第一旅政委。1942年7月调任中共西北中央局党校校长。1943年2月任中共绥德地委书记兼绥(德)米(脂)警备区和独立第一旅政委。1945年6月当选为中共七届中央候补委员。同年7月任陕甘宁边区集团军政委。<br />
<br />
　　抗日战争胜利后，曾任中共中央组织部副部长。1945年10月任中共中央西北局书记，兼任陕甘宁晋绥联防军政委。1947年起任陕甘宁野战集团军政委、西北野战兵团副政委、西北人民解放军野战军副政委。协助彭德怀指挥青化砭、羊马河、蟠龙战役，三战三捷。继又参与指挥陇东和三边战役。同年7月再次兼任陕甘宁晋绥联防军政委，与司令员贺龙统一领导西北地方武装和后方工作。1949年2月起任西北军区政委、中共中央西北局书记。<br />
<br />
<br />
　　中华人民共和国成立后，任中央人民政府委员、人民革命军事委员会委员，西北军政委员会副主席、代主席。1950年9月任中共中央宣传部部长兼政务院文教委员会副主任。1953年9月任政务院秘书长。1956年9月当选为中共八届中央委员。1959年4月至1962年10月任国务院副总理兼秘书长。<br />
<br />
　　1962年9月，在中共八届十中全会上，因“李建彤的《刘志丹》小说问题”，遭康生诬陷说习仲勋是为高岗翻案。康生还在全会中给毛泽东写了一个条子：“利用小说进行反党，是一个大发明。”习仲勋对这部小说创作，开始时有些参与。毕竟他与刘志丹是老战友嘛。从此，他失去职务。1963年，他受隔离审查期间。他是继彭德怀等人被“打倒”后，先于文革受到政治迫害的高级领导干部。1965年，他被贬到洛阳矿山机械厂担任副厂长。1966年冬，他被红卫兵抓起来到西安等地揪斗。期间曾被拉去老家富平批斗，却受到乡亲们的保护。1967年4月5日，他写信给周总理：“我的反面作用起完了，现在只是陪人挨斗了。”1968年1月3日，周恩来采取特殊保护方式，用飞机将仲勋从西安接回北京，交给卫戍区监护。然而，周恩来的保护毕竟是有限的。他被单独囚禁。如此累月计年，身心都受到严重摧残。1975年春，他被流放到洛阳的耐火材料厂。1978年，得到彻底平反。<br />
<br />
　　1978年3月被选为五届全国政协常委，后任中共广东省委第二书记、第一书记，兼广州军区第二政委。同年12月被增选为中共十一届中央委员。1979年任广东省省长。1980年兼任广州军区第一政委。同年9月被补选为五届全国人大常委会副委员长。1982年9月当选为中共十二届中央政治局委员、书记处书记。1988年4月被选为七届全国人大常委会副委员长。<br />
<br />
<br />
　　1990年10月，他基本从领导岗位退下来，到深圳休养。不过，在邓小平1992年南巡时，没有资料表明习仲勋此时与邓小平有过会面。1995年《习仲勋文集》出版。2002年5月22日习仲勋因病逝世。<br />
<br />
<br />
　　习仲勋在广东几年中，最突出的一点是忠实执行邓小平的改革开放路线。彭湃冤案、方方、古大存、冯白驹等所谓“地方主义”冤案等冤案的平反，是他作了很多努力的结果。当时，中国实行的严格的计划经济制度。而那被认为是正宗的社会主义，谁要是主动对其制度怀疑，仍有被“打倒”的危险。但习仲勋有话说。在1978年12月召开的中国共产党第十一届三中全会上，习仲勋一班人希望中央给予“地方处理问题的机动余地”和“吸收港澳、华侨资金”等要求。1979年4月的中央工作会议上，他又进谏：假如在广东实行特殊政策，广东就是“一大龙”，就不会有什么“四小龙”。“希望中央给点权，让广东先行一步，放手干。”“如果给广东一个特殊政策，经济可能几年就上去了。”广东想在深圳、珠海两市试办出口特区，希望中央允许他们这样做。邓小平显然被“特区”吸引住了，便将习仲勋单独叫去谈话。之后，邓小平便有了办特区的想法：“对！办一个特区。过去陕甘宁边区就是特区嘛！中央没有钱，你们自己去搞，杀出一条血路来。”深圳、汕头、珠海、厦门四大经济特区，就这么来的，仅把原来的“出口特区”正式命名为“经济特区”而已。后来广东经济发展特别快，广东人自夸自己“敢为天下先”，但广东人的胆子是由习仲勋那一班人率先推动出来的。<br />
<br />
<br />
　　1980年，军队被要求开展“兴无灭资”的运动。习仲勋兼任广州军区第一政委，参加贯彻这一运动的“广州军区召开政治工作会议”。会上，习仲勋站出来反对这场运动，说：“这个口号不那么科学，现在讲这个口号叫人难以琢磨，还是不讲好。不讲这个口号一样可以做好革命工作。”但省里的有些高官不同意习仲勋的意见。于是，广东省向中央请示。邓小平最后拍板，称这个口号不够完全。一场原本应当轰轰烈烈的军队政治运动因而化于无形之中。后来习仲勋调北京后，与一些极左派意见经常相对立。相对而言，他与胡耀邦的思想比较一致。文革中的“左王”之一王力差点被重新启用，就因为习仲勋等人的坚决抵制而避免他再度祸害国家的发展。由于习仲勋并未被又一次打倒，所以他对阻止中国走回极左的路子，作了一定的贡献。<br />
<br />
<br />
　　说来不相信，习仲勋的几个儿女在童年时代，主要是靠他照顾，而不是孩子的母亲齐心。齐心在1952年进京后，先在马列学院学习，然后留在中央党校工作。党校离家远，齐心的工作比时任宣传部长的习仲勋还忙，只能将照顾儿女的主要责任交给习仲勋。习仲勋后来就是当上国务院副总理，也经常抽时间亲自替孩子洗澡，为他们洗衣服。国务院有些活动，他为了经常一个人把孩子们带在身边，害得周恩来多次问他同样的问题：“怎么见不到齐心同志呢？”<br />
<br />
<br />
　　习仲勋对作家、艺术家，关系一直很好。1945年，他任西北局书记时，曾亲自领导由著名三十年代初上海电影演员陈波儿等创作电影故事片《劳动英雄》。诗人柯仲平、作家柳青、杜鹏程、剧作家马健翎等，都曾得到他的支持。1962年因李建彤的小说《刘志丹》受康生陷害，那也就毫不奇怪了。<br />
<br />
<br />
　　1986年年底，安徽、上海、北京相继闹起学潮。12月30日，胡耀邦召开政治局常委会，准备讨论书记处拿出的关于当前时局的意见。邓小平发脾气、摔电话，不许开常委会，令胡耀邦到他指定的地方去谈话。邓认为学运的发生是“几年来反对资产阶级自由化思潮旗帜不鲜明、态度不坚决的结果”。邓小平越说火气越大，他说：“没有专政手段是不行的。对专政手段，不但要讲，而且必要时要使用。……如果有人要制造流血事件，你有什么办法？”<br />
<br />
<br />
　　“你们这不是重演《逼宫》这场戏吗？”<br />
<br />
<br />
　　1987年元旦，北京市又爆发一场大规模的游行示威。元旦之夜，在邓小平家里，彭真、陈云、王震等紧锣密鼓地策划对策。胡耀邦还蒙在鼓里。<br />
<br />
　　三个月以前已在中共十二届五中全会上离开中共书记处的习仲勋，凭政治敏感已觉察到危机迫在眉睫，乘新年探问之机提醒：“耀邦啊，我在为你担心！”胡耀邦不在意：“我吗？不会有什么，党毕竟已经成熟了。”<br />
<br />
　　当天夜里，胡耀邦办公桌上的红机响起，是赵紫阳打来的：“耀邦，你现在干什么？”胡耀邦说：“还在研究学潮问题，我试图寻找一种更稳妥的解决当前危机的新路子。”赵说：“我劝你先别忙着干别的，还是马上找小平同志谈一谈，向他承认错误，检讨一下自己工作中的失误，争取主动为好。”胡耀邦说：“我并没有犯什么错误，你让我检讨什么？”赵紫阳急了：“耀邦同志，现在党内在一些重要问题上产生了分歧，小平同志是众望所归的政治家、军事家，我们应该尽量地尊重他。你常常讲，我们党再也经不起穷折腾了，你难道愿意看见我们党再面临一次分裂？”“再拖几天就晚了，你还是尽快向小平同志认个错，越快越好！”<br />
<br />
　　第二天，胡耀邦按照赵的建议，求见邓小平。谈到分歧时邓小平摆摆手说：“那都是工作中的正常范围，我们之间没有必要再纠缠了。我打算召开一次中央生活会，有什么问题大家摆在桌面上来谈，好好沟通一下思想嘛。”<br />
<br />
　只隔了一天，胡耀邦就接到中央顾问委员会的通知，到中南海小会议厅开所谓“生活会”，会议由中顾委副主任薄一波主持，他一开始就要求胡耀邦作“深刻检讨”。耀邦只讲了一两分钟，彭真就打断他的话，批判耀邦同自由化代表人物方励之、刘宾雁、王若望“打得火热”。杨尚昆接着说：“如果你想亡党亡国的话，那你就去和资产阶级自由化分子结成联盟吧。”王震说：“你胡耀邦要是不愿意和我们走的话，你不必待在这里了。”薄一波说：“耀邦整天到处乱跑，全国二千多个县你都快跑遍了。你是党的历史上几位主席、副主席、总书记能跑的最高纪录。这不叫指导工作，而是游山逛景，哗众取宠。”邓小平说：“耀邦召集政治局常委会，让办公厅的人通知我参加，我偏不去！我在别人面前不敢摆架子，在你面前还是有资格的。”宋任穷说：“我最不能容忍的是胡耀邦对待小平同志的态度。”<br />
<br />
 　　两天以后召开第二次所谓“生活会”，政治局又有几个人被邀请参加会议，其中有习仲勋、乔石、李鹏、胡启立。李鹏先发言，他说：“由于耀邦同志放松思想领域里的领导权，导致了自由化思潮在各高校的大泛滥。”<br />
<br />
　　首次被通知参加“生活会”的习仲勋忍无可忍，站出来说：“你们这是干什么？这不是重演《逼宫》这场戏吗？”他拍了桌子：“这不正常！生活会上不能讨论党的总书记的去留问题，这是违反党的原则的。你们开了这样的头，只会给将来党和国家的安定团结埋下祸根。我坚决反对你们这种干法！”耀邦急忙站起来劝说：“仲勋同志，我已考虑好了，不让我干我就辞职。”<br />
<br />
　　邓小平板着面孔对习仲勋说：“我当然不是说中央召开的高级干部生活会上要讨论耀邦同志总书记职务的去留问题，这个问题应该在政治局会议上决定。但是，难道不可以在生活会上发表一下个人的看法吗？近些年来，耀邦，也包括仲勋同志大讲特讲民主和自由，为什么今天在党的生活会上一波、彭真同志提自己看法的自由和民主都没有了呢？所以我历来讲，那些越是口头上讲民主的人，其实并不想真正给大家民主，而只是允许他自己搞民主和自由。我看这种‘自由化’只是彻头彻尾的资产阶级，就是这个道理。”<br />
<br />
<br />
　　在第一次“生活会”上，邓小平就决定停止胡耀邦的总书记的职务；在第二次“生活会”上，又确定了令胡耀邦辞职。实际上，政治局也无权罢免总书记，因为总书记是由中央全会选出的。<br />
<br />
<br />
　　习仲勋1988年4月被选为七届全国人大常委会副委员长。胡耀邦晚年体会到世态炎凉，视习仲勋将个人沉浮置之度外的支持为十分难得的慰藉，非常珍惜。<br />
<br />
<br />
　　“**”以后，习仲勋更在1990年一次全国人大常委会议上，指名批评对民主学潮诉诸武力的邓小平和杨尚昆。<br />
<br />
<br />
　　但习仲勋在最高决策圈逐渐边缘化，身体也恶化，于2002年逝世。习近平一位朋友透露，父亲病危期间，至孝的他一直服侍在病榻前，父亲对他更多不是“言传”，而是“身教”。<br />
<br />
<br />
　　据齐心叙述，习仲勋恢复工作后，自己是由在清华读书的儿子近平陪同去河南洛阳办理手续。这间接透露，习近平对父辈的坎坷遭际有切身感受。<br />
<br />
<br />
　　赵紫阳2005年1月去世后，习仲勋的遗孀齐心不避嫌，以其个人和“率子女”的名义向赵家送上花篮。这在某种程度上表明了习仲勋的态度，也在某种程度上表明了包括习近平在内的其子女的态度。<br />
 <br />
―― 原载: 作者博客</div>

							
							
							
							
