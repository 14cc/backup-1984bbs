																											<div id="postmessage_612605" class="t_msgfont">1、文化的差异还是人种的差异？<br />
<br />
很多人相信，一些社群比另一些社群的人更“聪明”，例如，美国社会里的华人和犹太人显得比黑人和拉丁美洲人更“聪明”。不过，这些“聪明”的差异到底是基于文化还是人种确需要仔细甄别。以华人为例，华人家庭更重视对子女的教育，鼓励“望子成龙”的文化传统，因此，华人显得“更聪明”不过是文化或生活方式带来的。不过，迄今似乎没有什么证据表明人类种群之间存在无可辩驳的“智力”差异。像老鼠说的，“智力”本身的概念语焉不详，对智力测验设计基于对智力学说的假设。像现在越来越多的心理学家和教育学家都接受多元智力学说，而在多元智力学说里，诸如对运动、艺术的能力都纳入到广义的智力范畴。<br />
<br />
2、如何看待人类种群的差异是政治正确的尺度<br />
<br />
人类不同种群之间确实存在差异，但如何认识这些差异却是另一个方面的问题。例如，黑人肤色黑适合在热带地区生活，白种人肤色浅适合在寒冷地区生活。从进化角度，所有的人种都源于非洲，而白种人之所以进化成较浅的肤色是因为方便在缺少阳光直射的地方合成维生素D。但如何看待肤色差异却是基于不同的观点。认为浅色肤色比深色肤色更“优越”的观点是臭名昭著种族歧视的理论基础。而试图从智力角度来论证人类种群之间有“高下”之别的努力，从一开始就很容易陷入种族歧视的陷阱，因为即使存在差异，而如何看待这些差异正是检验政治正确的尺度。你可以从不同人类种群之间找到很多差异，比如，黑人的肌肉爆发力比黄种人更强，白种人和黑种人的阴茎比黄种人更长，等等，而这些差异都完全可能是可以经由科学实证论证的事实 ，但现在的主流价值观是以多元化来看待这些差异的，也就是说，差异不是判断优劣的依据。<br />
<br />
3、人种的差异远小于社群的差异<br />
<br />
人类的每一种群都包含了各种特质类型的人，因此平均后的人类种群“面貌”之间的差异其实并不大（抛开文化而言），但人类不同社群之间的差异却可以大相径庭。例如，同性恋社群与异性恋社群在性取向上面可以完全不同，而同性恋在人群的比例与文化和人种似乎没有什么相关关系。再例如，侏儒社群和高个子社群彼此看待世界的视野完全可能大异其趣。这些社群之间的差异也完全可能成为歧视的根源。对同性恋者的残酷打压即使在现在公认最开明的北欧历史上也称得上罄竹难书。因此，现在文明社会的主流价值观教育是学会悦纳多元化，这就是文明的进步。而“多元化”正是看待差异的一个文明的角度。<br />
