													<h2>与平等有关的心理学问题</h2>
						
						
																											<div id="postmessage_612412" class="t_msgfont">与平等有关的心理学问题（刘荻）<br />
2010-05-06<br />
<br />
美国奥地利学派经济学家莫瑞&#8226;罗斯巴德(Murray N. Rothbard)在《反自然的平等主义》一文中，批判了西方社会中的左翼人士认为人和人之间没有或者不应有差异的平等主义观点，该文也以“红发者容易激动”为例，批评了心理学中的“刻板印象”概念。<br />
<br />
刻板印象是指对某一类人的比较固定、概括而笼统的看法，例如黑人擅长音乐和运动，中国人数学好，女人情绪化，等等。心理学家认为使用刻板印象来判断他人经常会发生错误。但罗斯巴德的文章似乎对这一概念有所误解：心理学家在使用“刻板印象”一词时，并不认为刻板印象一定是毫无根据的（事实上心理学家在这一问题上的观点与罗斯巴德相同，即认为刻板印象多少是有些道理的），也不认为各类人之间没有差异；心理学家认为使用刻板印象来判断他人经常会发生错误，原因仅仅在于对一个群体的笼统描述未必适合群体中的每个个体：或许平均来讲红发者确实比非红发者更容易激动，但具体到某个红头发的人，就未必如此了。心理学家在此问题上的观点是与他人交往时要多关注具体个人，而不是一味用对某个群体的笼统描述来判断他人，这与奥地利学派的“方法论个人主义”其实是不谋而合的。<br />
<br />
与之有关的另一个问题是，最近推特（twitter）上有网友说到：打个极端的比方，如果有一天，有人通过足够科学的方法证明了某一人种要比另一人种更聪明，那么那个时代哪一种人的面目会更丑恶？是像伽利略一样努力发现真相的种族主义者，还是像罗马教廷一样拼命掩盖真相来维持伦理的平等主义者？<br />
<br />
对此我的回答是：聪明的定义是可以改变的。现实点说，心理学家并不确切地知道智力是什么。心理学家们对智力最简单的定义就是：智力就是做智力测验的能力。当然，这是一个循环定义。不过心理学家们对智力有一些基本的假设，例如儿童的智力随年龄增长，成人的智力基本稳定，智力与儿童在学校的学习成绩有较高的正相关，等等。心理学家以这些假设为基础编制智力测验，与这些基本假设不一致的智力测验题会被清除出测验。例如有这样一道题，学校里成绩好的儿童大多答错，而成绩差的儿童大多答对，那么这道题很有可能无法进入智力测验中。<br />
<br />
智商测验最初的用途是对儿童进行测试，以便挑选出智力不足的儿童对其进行特殊教育。儿童智力测验采用比例智商，以100分作为某一年龄组的平均智商。如果一个儿童年龄为五岁，而智力年龄为六岁，则其智商为120。<br />
<br />
智商测验扩展到成人之后，由于成人的智力不再随年龄增长，因此比例智商不再适用，所以成人智力测验采用离差智商，即测试一个人的智力在其所在群体中的位置。离差智商以100分作为某一群体的平均分，以15分作为一个标准差，如果某人的智商测验结果为130分，则其智力属于所在群体中最优的2.5%。<br />
<br />
因此我们可以说，智商测验关心的是个人在群体中的位置，而不关心各个群体之间的比较。换句话说，智商测验规定各个群体的平均智商都是100分。智商测验无法证明一个人种要比另一个人种聪明，因为这违背了测验的前提假设。如果某一智商测验证明――比如说――白人比黑人分数高，那么该测验本身会被批评为带有种族偏见或者文化偏见，而测验编制者可能会调整其中的题目，使白人和黑人的平均分数没有差异，也可能会为白人和黑人分别编制测验，其平均分都为100分。从另一个角度说，人的智力可以说是多方面的，智力测验题也要包含多方面的智力。让我们假设男人数学能力更强，女人语言能力更强。这样如果某一智商测验证明男人的智商比女人高，那么这一测验就可能会被批评较多偏重男人擅长的数学能力，而忽视了女人擅长的语言能力，而测验编制者则会调整两类题目的比重，使两性在测验结果上没有差异。<br />
<br />
同理，智商测验也无法反映出整个人类的智商随着时代的进步而逐渐提高的趋势，因为在任何一个时代，人类的平均智商都被规定为100分。只有在研究不同时代的智力测验题时人们才会发现，今天的测验题要比过去难很多。<br />
<br />
那么，各个群体的智力是不是真的存在差异？我们不知道，这取决于智力的定义，而如上所述，我们并不确切地知道智力是什么。如果各个群体之间真的存在差异，那么这种差异是先天因素造成的还是后天的社会文化因素造成的？我们也不知道，要用实验方法来证明这一点，在目前还是一件极为困难的事。社会调查和相关研究或许能够发现差异，但是极难确定产生这种差异的因果关系。关于人类这个地球上最为复杂的生灵，我们不知道的东西还有很多。</div>

							
							
							
							
