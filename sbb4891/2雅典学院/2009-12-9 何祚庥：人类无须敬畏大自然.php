													<h2>何祚庥：人类无须敬畏大自然</h2>
						
						
																											<div id="postmessage_364730" class="t_msgfont">　　<br />
　　《环球》杂志2005年第2期<br />
<br />
　　印度洋大海啸，夺走的不仅是数以万计的人的生命，还从某种程度上打击了人类征服大自然的信心。从人类历史上看，每当遇到人类力不可拒的天灾，总会有人彷徨无措，有人疑惧人类自身的能力，有人对大自然产生敬畏的心理。<br />
<br />
　　然而，不少科学家认为，随着科学技术的迅速发展，人类自身能力和开发自然、征服自然的能力得到了前所未有的加强。印度洋大海啸余波未平，中科院院士、着名理论物理学家何祚庥在接受《环球》杂志专访时，侃侃而谈他对人类和大自然的关系所进行的哲学思考，显示出当代科学家对科学力量和作用的信心。<br />
<br />
　　■ 何祚庥<br />
<br />
　　人与自然，以谁为本？<br />
<br />
　　印度洋大型地震海啸给人类最大的教训，就是折射出我们在对人和自然的关系上，认识需要深化。这几年来，“天人合一”的说法甚为盛行，有人把“天人合一”解释为人和自然相处和谐。对这样与词语原意不甚相符的解释我并不反对，但是我认为，人类要看到天与人之间不完全是和谐的一面，还有不和谐的一面。<br />
<br />
　　如果我们在人天关系上没有一个冷静的估计，就会放松警惕，导致灾难性的后果。这个问题从哲学角度来看更加值得反思。从历史来讲，在人类发展的早期，人类对自然的抵御能力很有限，所以一些进步思想家强调人定胜天，鼓舞人类与自然界作斗争。但随着科学技术的发展，这些年来人类的能力越来越强了，好像自然界的许多问题人类都能够解决，所以目前有些人生怕人类对自然界破坏得太过分，以至于反而对人类不利，所以强调要和谐。<br />
<br />
　　我以上的这番话是有针对性的，针对的是一些环境学家和生态学家。许多环境学家和生态学家提醒社会公众要注意保护环境，保护生态，这是有道理的。因为这些年来，人类生产力高度发展，的确有些环境和生态不必要地被破坏了。但我还是要说，我们有的环境学家和生态学家有一些片面的地方，也就是认为环境和生态是不能动的，一切“改造大自然”的主张都遭到某些环境学家、生态学家的反对。然而这种观点不符合人类的利益。<br />
<br />
　　在这里，我要尖锐地提出一个理论问题：我们在处理人和自然的关系时，奉行的应该是以人为本，还是以环境为本，以生态为本？这是个深刻的哲学问题。<br />
<br />
　　我认为，应该是以人为本。我绝不反对保护环境和保护生态，但需要弄清楚一个观念，保护环境和生态的目的是为了人。有些时候我们需要“破坏”一下环境、生态，改变一下环境和生态，但也是为了人。<br />
<br />
　　这个问题要搞清楚，因为它牵涉到实际工作。现在中国电力短缺，需要开发水能，需要修水库，这就不可避免要破坏一些环境和生态。这里有一个权衡得失的问题，如果过分强调保护环境和生态，那么水库就不能建设。我认为，遇到这样的情形，权衡轻重得失的标准就应该是以人为本。胡锦涛总书记在《树立和落实科学发展观》的讲话中指出，坚持用科学发展观来指导人口资源环境工作要注意把握好几点，其中第一点就是“要牢固树立以人为本的观念”。<br />
<br />
　　我觉得，要真正树立以人为本的理念，是一个大的课题，我们的思维不能片面。回到人与自然的话题上，就是我们也要看到大自然和人类不相友好的一面。<br />
<br />
　　从某种意义上讲，如何进一步改造大自然，使大自然的改造后果符合人类发展的利益，这个问题是非常重要的。这个意义值得一些环境学家和生态学家深思。<br />
<br />
　　驳“人要敬畏大自然”<br />
<br />
　　这次海啸给人类另外一个启示是：人类在自然灾害面前不能无所作为，还是要有所作为。<br />
<br />
　　我要严厉批评一个口号，即所谓“人要敬畏大自然”――一种对人和自然的关系无所作为的观点。我认为，该防御要防御，该制止就制止。我们要尽可能减少自然灾害给人类带来的损失，但并不意味着要敬，要畏。特别这个观点是在当时“非典”盛行的时候提出的。人类对“非典”怎么敬，怎么畏？这个观点实际上将人与自然的关系伦理化了。这个由某位副教授在中央大报上发表的文章中提出的口号，实际上是批评科学主义，认为人类不该利用科学来有所作为，反映到人和自然的关系，就是敬与畏，不要老想去改造自然。这就在实际上走向了“反科学”。<br />
<br />
　　应该看到，近代科学才400多年的历史，从伽利略开始到现在，科学水准的发展是极为迅速的。为纪念科学的发展，纪念爱因斯坦发表狭义相对论、光电效应理论100周年和爱因斯坦逝世50周年，联合国将2005年定为“国际物理年”。<br />
<br />
　　科学是加速发展的。400年如此，4000年，4万年又会如何？要相信科学的作用和力量，要看到科学的未来。<br />
<br />
　　到目前为止，人类对自然规律的认识还停留在初级阶段，很遗憾这次大海啸人们没有预见到，这是科学水准的问题。坦白地说，人类对地震的有些预测的根据不是特别充分的。直到地震海啸快要发生了，才能有所警觉。当前人类要阻止自然灾害的发生是不可能的，但人类还是可以做一些预警工作。对于地震海啸，关键在于防御。房子要结实些，抗震结构要好些，这些比预测还要重要，因为这些我们能够做到。</div>

							
							
							
							
