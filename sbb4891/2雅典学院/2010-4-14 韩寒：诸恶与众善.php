													<h2>韩寒：诸恶与众善</h2>
						
						
																											<div id="postmessage_562178" class="t_msgfont">最近有一篇，流传甚广，叫《不要给西南灾区捐水了》，署名是韩寒。这篇文章并非我写，我的所有杂文的出处都会在我的博客中，如果博客里没有出现过（注意，是出现过，因为我不能保证文章出现以后能一直出现着），那就是没有写过。这篇文章我大致看了，很明显，文中类似“我曾经说过，如果我愿意，我可以去颠覆你们二十多年来形成的价值观，因为生活中很多在你们看来是理所当然的观念都是错误的”这样的话，是不会出现在我的文章里的。<br />
<br />
对于本文背后作者的观点，我认同一部分，但对于主观点我不认同。在四川地震前，我已经知道原来红十字会是有一个所谓的手续费的，这个手续费的比例很高。到了四川，我们去了红十字会，当时老罗和我说起此事，我和老罗还说，如果捐款很多，岂不是光手续费就能够收几十亿？我寻思着要不要写这个文章，但是到最后，我都没有写，因为我不能在那个时候打击大家捐款的热情，这个近些年一直表现的自私冷漠的民族，多么难得有如此团结向善的时刻。后来我只是说，我不会向官方机构捐款，手续费是一方面的问题，另外一方面我并不了解最终捐款的去向。好在最终红十字会宣布免收手续费。<br />
<br />
至今我一直有一个疑惑，就是比如某处灾难，救灾最终需要一亿，民众积极捐款，捐到了五千万，那到底意味着救灾总款变成了一亿五千万呢，还是救灾款依然是一亿，但是我们捐给了政府五千万？它困扰了我很久，最终解决的方法是各帮各的，各行其善。<br />
<br />
西南大旱，天灾人祸都有，无论一个政府做的有多么不到位，都不能妨碍你以个人的身份行善的决心。事实上，经过了汶川地震，震后又出现了一些让人失望的新闻，大家的善心抗震程度又有所提高，之后的几次天灾，民众的热情程度一直不是很高，包括这次西南大旱。但你需知道，也许只有你知道，在你的一身中，一定犯下了罪孽。虽然在这个压力这么大的社会里，我们恨不得都是需要扶助的对象，但是力所能及的慈善，不光是为了让这个世界更加有希望，也是为了减轻你自己的罪恶，这个事情和政府无关，但是和社会有关<br />
<br />
有一句话：诸恶莫作，众善奉行。但是如果诸恶一直在作，甚至越做越过，乃至是非颠倒，这一切都不影响后面的那句，众善奉行。<br />
<br />
