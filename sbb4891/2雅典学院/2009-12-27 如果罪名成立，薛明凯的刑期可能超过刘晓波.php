2006年曾在山东兖州第一中学读书的薛明凯，今年四月因在网络上发表批评政府的文章以及加入海外民运组织中国民主党，随后被深圳宝安区公安拘捕，九月被控以“颠覆国家政权罪”，案件在深圳法院开庭。薛明凯的母亲王女士星期四对本台表示，起诉书指控他儿子加入境外敌对势力：“起诉书上指控零六年七、八月间，在网上散布对共产党不满的谣言，联系张军（音）、吴思陆（音），他啥都没见过面，将组建民主公民党，从来没建成，到了零九年说他联系在美国的中国民主党主席谢万军，4月12号加入到这个民主党。”<br />
<br />
王女士表示，儿子于今年4月12号在网上加入中国民主党：“不知道谢万军的民主党是（当局所指的）敌对组织的，5月8号逮捕的”。<br />
<br />
