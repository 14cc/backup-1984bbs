山东青年薛明凯因今年4月在互联网加入海外政治组织中国民主党，今年9月被深圳法院以“颠覆国家政权罪”开庭审理。薛明凯的母亲王女士星期四对本台表示，儿子只是在网络上加入海外组织，并没有实际参与活动。律师为他做了无罪辩护。自由亚洲电台特约记者乔龙报道 <br />
