													<h2>我们要提防“民主幼稚病”</h2>
						
						
																											<div id="postmessage_823228" class="t_msgfont">前段时间发现大家对温家宝在深圳的讲话有很大意见，很多人认为他是作秀，影帝。我觉得亦有道理，但是我们不妨从另外一个角度再来思考，或许会有新的启迪。以上文章转自“熊飞骏--博客中国”<br />
<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;刘亚洲将军的“十年民主论”和温总理在深圳关于推进政治体制改革的讲话，给长期“停滞维稳”的中国政治吹来一股清新的空气。<br />
<br />
刘亚洲将军是特色中国少有的有远见负责任的政治家；温总理开明务实的政治风范也一样难能可贵。<br />
<br />
他们是体制内的“良心开明”派，代表中国的希望和未来。<br />
<br />
温总理和刘亚洲将军的“政治新思维”招致特权腐败力量和毛左独裁派的攻击非议自在意料之中，把握未来的智者永远也不可能被急功近利之徒所理解。<br />
<br />
令人痛心的是：有些自我标榜的“民主”人士也对温刘的“政治新思维”颇有微词。<br />
<br />
前几天在邮箱里意外看到一篇陌生人传来的某位海外人士的文章，大意是指温、刘近期表现出的开明思想是“作秀”，是别有用心，是企图麻木民主人士的斗志，是配合专制独裁力量“演双簧”……<br />
<br />
既然没有任何背景的我都能看到这篇文字，相信有很多倾向民主的国民也能看到类似的文字。这些文字虽然破绽百出，但却在国内倾向民主的群体中引起很大的思维混乱。<br />
<br />
我感觉写这类文字的人要么犯有不可救药的“民主幼稚病”；要么就是真正的“别有用心”。<br />
<br />
我想起了百年前中国的维新变化运动。<br />
<br />
那时的满清统治集团分为三个派别：满贵独裁派、排外维稳派和政治改良派。<br />
<br />
满贵独裁派以出身满洲贵族的青年军官为代表，主张朝中完全排除汉族文臣武将，在全中国实行满洲权贵清一色的专制独裁。<br />
<br />
排外维稳派以大学士徐桐为代表，主张稳定压倒一切，拒绝任何形式的政治变革，代表绝大多数腐败特权阶层的利益。徐桐这个人有必要作点特别的介绍，他直到十九世纪末还坚定地认为世界上除了中国外只有英吉利和法兰西两国，其它的国家都是两国捏造出来吓唬中国人的。<br />
<br />
政治改良派以直律总督李鸿章为代表，主张中国应该学习西方的宪政体制，认为政治体制改革迫在眉睫，否则洋务运动取得的经济军事成果就会付诸东流。<br />
<br />
以康梁为领袖的维新党作为中国朝野主张维新变法的力量，应该与统治集团内以李鸿章为代表的政治改良派团结起来，争取改造排外维稳派中的良心人士，孤立打击满贵独裁派，为维新变法事业结成尽可能广泛的同盟军，在朝野赢得政治变革的共识。<br />
<br />
可我们的变法斗士尤其是那些一味好斗不知妥协的激进青年却在关键时刻选错了斗争对象，不是把主要的斗争矛头指向满贵独裁派，而是指向李鸿章？<br />
<br />
变法斗士不信任甚至打击李鸿章似乎有充足的理由：<br />
<br />
１、 李鸿章是既得利益阶层，一样腐败弄权。<br />
<br />
２、 李鸿章说得多做得少，“作秀”成分很明显。<br />
<br />
３、 李鸿章签订了《马关条约》，是卖国贼。<br />
<br />
上述理由也许是事实，但对李鸿章很不公平。<br />
<br />
一、 专制体制到了后期几乎无官不腐无权不弄。李鸿章能够升到直律总督那样的高位，腐败弄权自不可免。这是专制体制造成的，不全是李鸿章的过错，不那么做他就会被体制无清淘汰出局。相对于官场这个腐败整体来说，李鸿章还算好一点的。<br />
<br />
二、 李鸿章不是不想“做”，而是专制体制内他的权力有限，根本不可能做上面不允许的政治大事。<br />
<br />
三、 签订《马关条约》不是李鸿章的错，如果没有上面的旨意，一个外交官有胆量和权力签订割地赔款的卖国条约吗？如果当时号称世界第六大海军强国的中国没有被海军力量屈居十二位的撮尔小国日本打得全军覆没，他用得着签订割地赔款的条约吗？要怪只能怪慈禧太后集团；只能怪跑关系内行打仗外行的腐败军队，怎么能怪他这个只有奉命签字权的外交官呢？<br />
<br />
如此浅显明了的道理，我们的变法斗士就是不明白，依旧在李鸿章身上浪费火力，继续亲者痛仇者快的闹剧。<br />
<br />
变法斗士的狂热终于把以李鸿章为代表的政治改良派有效孤立起来，使满贵独裁派和排外维稳派联起手来，在统治集团内赢利了压倒的优势。<br />
<br />
那些同情支持维新变法的人士也因为变法斗士“敌友不分”而失望心冷，纷纷选择远离维新变法的阵营。<br />
<br />
朝中的政治改良派被孤立起来后，维新变法力量也接着转入孤立无援的绝境，很快被朝野的专制腐败力量联合绞杀。<br />
<br />
中国第一次现代化变革运动就这样被淹没在血泊之中。<br />
<br />
<br />
一百年后的今天，中国又回到了与维新变法相似的政治风景。<br />
<br />
我们的执政集团一样分为三股互相消长的政治力量。<br />
<br />
温总理和刘亚洲将军无疑是良心开明派的最杰出代表。<br />
<br />
当民主幼稚人士对温刘的“政治新思维”不理解不信任甚至于恶语猜疑时，是不是中了反民主势力的圈套，犯下和当年变法斗士一样的错误呢？<br />
<br />
<br />
民主是宽容妥协合作的政治；就算是以暴抗暴的民主革命也不能玉石俱焚。真正的民主变革必须吸收特权集团内的良心进步力量！企图把特权集团做为一个整体打倒清算的变革都不是“民主变革”！<br />
<br />
蒋介石当年发动四一二政变进行清党，使国民党从一个民主政党退化成专制独裁党。<br />
<br />
法国大革命期间，雅各宾党人把王党集团和特权阶层全部送上了断头台，结果有效扼杀了法国的民主事业，换来了军事独裁和专制复辟。使法国发生了近一个世纪的大倒退。<br />
<br />
民主事业不是平民的专利，贵族一样能很好地理解与把握民主。英国的民主事业就主要是贵族的贡献。把贵族完全排除在外的民主变革一样不是真正的民主。<br />
<br />
即使再专制的特权集团，内部一样有良心正义人士，国家民族的民主进步离不开他们的努力和奉献。如果机缘巧合，他们甚至是推进国家民主进步的中坚力量。<br />
<br />
戈尔巴乔夫和叶利钦是苏联民主变革的首功元勋。他们一个是苏共总书记，一个是莫斯科市委书记，都是体制内举足轻重的政要；并且都曾腐败弄权过，否则在逆淘汰专制官场不可能升到那样的高位。<br />
<br />
