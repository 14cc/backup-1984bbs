皮诺切特并非搞经济的行家里手，但他知人善任，组建了一个文人经济班子，走专家治国之路。他采纳被称为“芝加哥学派”的经济学家们的意见，首先在智利实行以促进出口和吸引外资为导向的自由市场经济模式。在没有议会 ... </blockquote></div>在中国，这是不可能的。要可行，袁大头早成功了。如果中国只有台湾岛那么大，或许皮诺切特这种新权威主义的尝试尚有希望。关于自由派和新权威主义以及新保守主义的争论，从89那会儿就开始了。<br />
<br />
说回来，真要催生中国的皮诺切特，最可行的方法也恰恰不是自下而上的革命，而是利用土共的权威促使其接受自由市场 -- 这就是新保守主义思想的精华，说白了，就是不再对土共砍头，而是对其洗脸，将其和平演变成中国的皮诺切特（其实邓小平不就是中国皮诺切特一世么），以武力和法西斯高压政权彻底摧毁来自底层民众对私有化的反抗，剥夺起码的国家福利和民众组织（比如工会）... 这些现在土共早就做到了。<br />
<br />
唯一让自由人士不满意的是，土共除了具有法西斯特性，还有很重的民族主义色彩，受到民本思想的强烈制约，不会随随便便的象皮诺切特那样象西方市场彻底放开，沦为100%的附庸，不可能100%的民进国退。它有太多的历史政治遗产（不同人眼里，有的叫遗产，有的叫负担），因此它不可能100%的转变成中国的皮诺切特。<br />
<br />
总之，大家各怀鬼胎，都希望中国向自己设想的方向发展。就好比这帮中国皮诺切特地粉丝，判断给土共继续洗脸有戏的，就会进入土共的体制，希望改造土共同时也给自己在体制内捞足好处，现在的体制内经济学家不少都是这个样子吧？<br />
<br />
判断给土共洗脸没戏的，就像关羽你这样，觉得以来土共作中国的皮诺切特没希望，于是幻想通过发动所谓的“资产阶级”革命，踢开土共自己干，中国的皮诺切特土共作不来，你就自己做。就像当初的希特勒嫌魏玛那帮人太脓包一样。可惜，相对当年的魏玛政权，土共实在是太强大了。就算是魏玛政权，希特勒的啤酒馆政变照样被秒杀。最后发现，要夺取政权，还得靠已经假设好了的民主选举程序。<br />
<br />
中国的皮诺切特要上台，就我看，可能有两条路：<br />
<br />
一条是继续给土共洗脸，试图从内部改造土共 -- 但这条路，阻力来自于土共欠债太多，无数的自由派/民主控/地主老财后代/独轮运都和土共势不两立，你要和土共合作，给他洗脸，你就等于同这无数潜在的同盟者决裂，相当长一段时间背上帮凶走狗的恶名，彻底丧失道义资本，很可能最后也只能以帮凶走狗的恶名终结自己的政治生命 -- 刘晓波一句我没有敌人，尚且招来恶评一片，何况尔等？<br />
<br />
另一条道路就是放弃土共路线, 曲线救国, 先全力同一切召唤民主的势力合作 -- 自由派民主控独轮运甚至毛左, 目的就是搬倒土共或者至少让其接受民主化改造放弃绝对权力, 然后在利用民主框架获取实际权力, 在利用这权力清除一切异己原形毕露 -- 说白了就是当年纳粹玩的那一套. 10年前和冉云飞把酒言欢, 一起上街要民主要自由, 把自己伪装成自由派; 10年后一朝权在手便把令来行, 冉云飞这种人失去利用价值后一脚踢开, 要是还唠唠叨叨, 就直接装麻袋扔江里去... 这条道路也可行, 但也险阻重重:<br />
<br />
第一, 土共太强大了, 说不定没有特殊机遇100年也别想搬倒他, 这是最现实的.<br />
第二, 就算搬倒土共, 中国民主了, 中国未来的皮诺切特们如何保证自己能在民主的竞争中占据上风? 中国人性格就是这样, 偿到甜头前绝对保守, 一旦成了既成事实再想变回去又会死活不答应. 当年满清倒台前, 多少中国人泛嘀咕, 中国没皇帝行吗? 满清一倒台, 大家偿到了无法无天的好处, 再有人要做皇帝, 就连当年的保皇党也不答应了. 土共的独裁大家虽然骂, 但一般还能混, 因为大家在土共下面都差不多. 土共一旦倒台, 民主了, 民选了, 你再想搞独裁, 成为中国的皮诺切特用皮鞭坦克为中国的私有化自由市场保价护航, 那天底下所有的中国人除了你的跟班就都会成为你的敌人 -- 人人巴不得少一个争权的, 有你撞枪口上, 人家乐还来不及呢. 当初老袁多牛逼, 占着大总统位子, 手里还有几十万北洋军, 最后照样土崩瓦解, 你比老袁如何?<br />
