<br />
【明报专讯】美国专家声称已经查出攻击Google的中国黑客，利用Internet Explorer浏览器漏洞侵入计算机并安插间谍软件。外电报道称，此人并非「全职政府员工」，而是自由职业安全顾问，为了能从事自己所擅长的研究，不得不听从政府的指示。<br />
<br />
据英国《金融时报》报道引述「为美国政府工作的一名研究人员」透露，他们查到一名30多岁的自由职业安全顾问编写了一个程序，可利用Internet Explorer浏览器此前未知的一个安全漏洞，侵入计算机安插间谍软件。此人还将程序的片段贴到一个黑客讨论区上，称其为自己正在「努力完成」的东西。<br />
<br />
科研专才举动受监视<br />
<br />
据美国调查组介绍，他们发现这人并非全职政府员工，他未曾发起攻击，而且事实上不希望自己编写的代码被用于攻击。「如果他想从事自己所擅长的研究，他必须不时听从当局的指示」。美国专家表示，「他不希望一举一动被监视，但像他这种技术水平的人逃不过那样的事。国家能够优先获得这些研究人员的工作成果。」<br />
<br />
报道称，「这将使得中国政府否认涉身其中的难度明显加大」。美国前网络安全官员、现就职于一间网络安全公司的米歇尔．权（Mischel Kwon）表示，「我们正意识到，这个问题不只是技术层面的，还有其它层面，其它政府部门也需要介入。」<br />
<br />
「北京否认涉事难度增」<br />
<br />
此外，《华尔街日报》亦引述「知情人士」称跟踪到一个「亚洲黑客组织」，规模较小、更有针对性，只窃取特定数据，攻击亦仅针对公司而非美国军方或政府机构。但未清楚该黑客组织是否与中国政府有关，或是以政府利益行事的所谓爱国黑客组织。<br />
<br />
今年1月13日，美国搜寻器网站Google宣称自己受到来自中国黑客的攻击，有两名维权人士的Gmail账户被侵入。上周六，《纽约时报》称调查组查到攻击源自上海交通大学和山东蓝翔高级技工学校的计算机。</div>

							
							
							
							
