													<h2>回复 9# 余含泪 的话题</h2>
						
						
																											<div id="postmessage_24085" class="t_msgfont">政治避难申请案例分析<br />
<a href="http://book.263.com/20040705/00415763.html" target="_blank">http://book.263.com/20040705/00415763.html</a><br />
<br />
朱雷安倒是一点儿也不担心。他除了让他老婆怀上了孩子之外， 还又帮着她申请了政治避难。听起来像是天方夜谭，但是，不论是来自山区的农田还是来自家 庭手工业发达的小镇，怀着发财梦想的温州老乡偷渡到法国后，却几乎无一例外地去申请政治避难。二战的硝烟刚刚散去，人们对战争期间种种大规模的种族、政治迫害记忆犹新。法国于1951年7月28日签署了刚刚诞生的联合国难民公约，该公约至今已经得到上百个国家的签署。公约规定，如果有人因为他的种族、国籍、宗教信仰或是所属的社会团体而遭到迫害，或是有理由害怕遭到这样的迫害，难民公约的签署国必须以政治难民的身份对他实施庇护。法国难民署于1952年7月25日成立。每年都有来自世界各地的移民在法国申请政治避难。<br />
<br />
<br />
&nbsp; &nbsp; 在朱雷安替他老婆申请政治避难的1996年，法国难民署总共收了 两万多份申请，最后得到难民身份从而在法国合法居留下来的人数为 3000多人，在这3000多人里，没有一例温州人！可以从20世纪80 年代初来的温州老乡，问到上个月刚刚非法踏上法兰西国土的黑移民，他们中间可能没有一个人见过难民证的颜色，但每个人都会知道申请 表和拒绝信的样式。实际上，没有一个温州非法移民真正想得到政治 避难权，但也几乎没有一个不去申请。不是跟着他，我怎么也不会有机会到这个地方来。这里是巴黎北郊 的一个空地。北环城线高架桥就在不远处，轰轰的车流不间断地通过。看得出原先这里应该有个什么建筑，给扒平了，空出一大片高低不平的黄土地面，许多建筑垃圾还没有被清除。空地中间大概有几十个温 州人和百来个黑人、北非阿拉伯人、土耳其人围着两辆废弃的家居拖 斗车厢拼挤着。车厢的玻璃窗紧闭着，各挂着一个纸牌，一个上面写着A －H，另一个写着I－Z。朱雷安就往围着I－Z的车厢的人堆里扎。<br />
<br />
&nbsp; &nbsp; 我明白了，这里是法国红十字会为这些无公开地址的非法移民提供的临 时通讯处，以供收发信件。远远路过的车辆有时会放慢速度，看看这 里究竟发生了什么。总是不得甚解而去。谁也想像不到，这群外国人 在按照日内瓦难民公约的条例，在这块空旷的建筑工地上履行申请政 治避难的程序。 在法国，申请政治避难的程序在1990年经历了一次大改革，新的办 理方法延续至今。在1990年前，一个外国人，踏上法国的国土后，去 申请政治避难，难民署一般会用上三年时间来处理他的申请。在处理 申请期间，申请者会获得一个临时的工作许可。三年后，根据难民署 处理的结果，警察局再做出是否给予其居留权的决定。如果政治避难 申请被接受，警察局自然会发给申请人居留证。麻烦就麻烦在申请被 拒绝的情况下，在三年或者是三年多的等待里，申请人往往在法国已 经建立了一个正常的生活，有了一个固定的工作，甚至是结了婚、生了孩子。这时候再勒令他离开法国，等于是把他从他自己的社会和个 人生活圈里再活活剥离出来，在道义上既难以为法国社会接受，在实 际上也难以操作。法国就有过一个中学的学生集体罢课，抗议警察局 勒令他们的体育教师，一个政治避难被拒绝的申请人出境的事件。<br />
<br />
&nbsp; &nbsp; 绝大部分避难申请被拒绝者，并不离开法国，于是他们在合法地生活了 几年之后，又沦落到非法的境地，被迫离开原有的工作。他们的处境， 成了不少左翼人权组织以及媒体的关注对象。这类难以驱逐的非法移 民的产生，政府的政治难民申请程序负有很大的责任。1990年，正值 法国左右两党政权交替，在移民政策上以更显宽容和人道为特点的左 派社会党执政，总理米歇尔・罗加就采取了一项大胆的改革措施，他 开始对处于这种境地的申请被拒者进行身份合法化的大赦，1991年，有五万非法移民，其中有许多中国人，因此拿到了居留证。同时，罗 加总理将难民署的预算提高了三倍，增加人员，将政治避难申请从审 查到回绝的期限从三年缩短到三个月。因为绝大多数申请，其无效性 是一目了然的。这类申请人，在三个月后就会收到难民署的拒绝信， 审理被拒绝的申请人的上诉的期限，也不超过三个月。上诉被驳回后， 申请人就会被警方要求离境了。<br />
<br />
&nbsp; &nbsp; 而且，在不超过六七个月的全部审查 期内，不允许申请人工作。由于不许工作，理论上被申请人的生活就没有来源，尽管实际上他们都会去打黑工，政府于是用每月每人发1300 法郎的生活补贴的措施，解决了这个理论问题。在法国，由于审理政 治避难申请过长而产生的不可驱逐的非法移民，就此绝了迹。 但是，这个改革，并没有降低中国非法移民申请政治避难的积极性。朱雷安遵循的申请程序是这样的：带上中国护照或者身份证，加上翻译件，去警察局，申明自己要申请政治避难。如果连以上两种文 件都没有，出生公证也能被接受；可以解释为自己是在政府的追捕下逃离中国的，自然就不能随身携带官方身份证件。警察局会发给一份简要的表格，以及一个月或是几个月后的约会通知书。简要的表格要 求填写的内容是个人身份资料，扼要的申请避难的理由，如何来到法国的途径和在法国的地址。申请政治避难，申请人和难民署及警察局 会有很多信件来往。难住申请者的，往往是简表中地址一项：他们没 有合法租用的房子，也不能合法租用一个房子，住处的电费单都是顶 着别人的名字，所以没有任何文件来证明自己身居何处。<br />
<br />
&nbsp; &nbsp; 只有找亲戚朋友帮忙，开一张住房证明，即由房东或是合法的房客出具证明信、 附上房契或是其他租约文件，证明向申请人提供了住房。但愿意如此 帮忙的亲友也不是这么容易找到的。所以渐渐地在温州老乡中，出具一张住房证明就有了一个市场价码，从2000到3000法郎不等。后来，一些民间协会开始协助这些申请人，给他们提供通讯地址。法国红十 字会成了其中最大的一个机构。朱雷安的老婆在警察局表明申请政治 避难的意愿，领了简表之后，先到一家红十字会的办公室登记，得到 一份通讯地址证明，再就到了中国城的一家小翻译社，花了200法郎， 让人把申请表填了。按照约会时间二去警察局，将地址证明和简表交 上，换来由难民署印制的申请政治避难的大份表格和一张一个月的临 时居留证，上面注明不许工作。自从红十字会提供地址证明，政治 避难申请就变得简单易行了。申请人得在一个月内，将这个大份表格填好，交到难民署。表格 要求申请人详尽写明申请理由、提供遭受迫害的证据等。朱雷安又 回到那家翻译社，让人把大份表格填了，交到难民局，领到一张递交 申请的回执。<br />
<br />
&nbsp; &nbsp; 这时，在登记了他老婆名字的红十字会办事处，就会收 到难民署寄来的第一笔邮局汇款，2300法郎，作为申请者的生活补助。第一张一个月的临时居留证到期时，只需去警察局出示难民署的回执，便可领到一张三个月的临时居留证，这就是等待难民署的回答所需的时间。在这三个月中，朱雷安老婆每月都能在红十字会收到难民署寄 来的1300法郎的汇款单。因为来登记通讯地址的申请人太多，巴黎红 十字会就用两挂家用拖斗车厢充做信件发放中心，并在郊外找了这么个空建筑工地置放，以便容纳人流。通常情形是三个月未到，难民署的回绝信就到了，以申请人提出 的理由无证据为由，拒绝政治避难申请。但是，事情到这儿并没有结 束：朱雷安还是可以再去一趟翻译社，让人写一张&quot;上诉信&quot;，寄给难 民署的上诉委员会，表示不同意难民署的决定，让上诉委员会重新审 查。凭上诉委员会收到上诉信的回执，他还可以去警察局，再次领取 三个月的临时居留，享受三个月的生活补助，以等候上诉委员会的裁决。<br />
<br />
&nbsp; &nbsp; 而上诉委员会则会在一个月左右的时间内传唤上诉人出庭，并可 以提供中文翻译，以听取其陈述。但是，和所有的温州老乡一样，朱雷安是不会去享受这个法律权益的。也许是觉得去了也没用，或者是 觉得总不能脸对脸丢脸吧，他会坐等两个月后上诉委员会驳回他的上 诉。难民署的生活补助停止，警察局会给他一张在一个月内离开法国 的出境令。申请政治避难的游戏也就到此结束。这个游戏使初来乍到的非法移民获益匪浅。能得到一些生活补助 不说，有六七个月左右的合法居留期，可以坦坦然然地乘坐地铁、公 共汽车，穿行大街小巷，习惯一下这个决心一辈子扎根其间的陌生的 国度，为即将到来的漫长的非法生活做些必要的准备。更为重要的是，按法国的法律，非法移民在法国连续生活一定期限后，可以获得身份 的合法化，这个期限，从1997年后被定为十年。经验也告诉大家，20世纪80年代初、90年代 初和1997年的三次非法移民身份合法化特 赦，都有在法国居住时间的要求。黑移民们必须提供有效的证据，以证明自己何时到法国的。偷渡者自然不会有入境证明，政治避难申请、 勒令出境通知在这几次特赦中曾经是到达法国时间的最为有效的官方 证据。对于温州的非法移民朋友来说，申请政治避难，等于是报个到。<br />
<br />
&nbsp; &nbsp; 然后再把所有的申请避难的文件，包括勒令出境信小心地保存起来， 期待未来某一天能派上用场。 朱雷安老婆的避难申请还没到勒令出境阶段。他老婆的申请报到 难民署才两个月。他今天来一是看看汇款单有没有到，二是看看是不 是有了回绝信。他和我说他的回绝信应该比别人来的晚，因为他找了 一家很好的中国人开的翻译公司，是个朋友带去的，在中国城的一条小街上。据这位朋友说，这家翻译公司申请信写得特别好，别人的信，难民署三个月就回绝了，这家公司写的申请，都会拖五六个月才会被拒绝。所以价格也 就贵些，通常填写难民局的大份表格是300法郎，而朱付了900法郎。我问朱这个翻译找到了什么样出色的理由让难民 署的官员迟疑地做不了决断，朱雷安一脸茫然：这个政治避难的申请 者，居然不知道自己遭到过何种&quot;迫害&quot;！这时人群开始出现骚动。拖斗车厢的玻璃窗拉开了一条缝，大家一齐往里挤。车厢里一个法国老太努力抵住窗玻璃，大声喊着，别挤别挤！朱雷安本来是站在外圈的，这时掏出他老婆的临时居留证，三 下两下就挤进了内圈，我看见他一扬手，就把临时居留证准确地从小窗口扔了进去。<br />
<br />
&nbsp; &nbsp; 手脚还真是麻利。他所说的翻译公司我基本了解，因为温州老乡大多不通法文，所以需要翻译帮助。申请避难的表格，也多由这些翻译公司填写。填写 警察局发的第一份简表，200法郎，难民署印制的大份表格，300到400 法郎，上诉信分长短不同，300到500法郎。从每个避难申请者身上，能赚上800到1000法郎。每当从这些小公司的门前经过，看见里面总 有五六个、七八个不等的客人坐着排队，就可以判断出他们的生意还真 不错。尤其是那些申请信的样式在公司电脑里都存着，无需做重复劳 动，改个申请人姓名就行。大约有五六个申请理由，供客人挑选。有的客人更好说话， 也不挑选，让公司随便给一个就是。也许朱雷安是属于这一类客人，所以连他自己也不知道申请表上涂了些什么。朱雷安从人堆里挤出来，他把表格的复印件带来了，我看了看，这个花了他900法郎的申请理由,几十行字，法语表达不伦不类，语法词法拼写错误随处可见。拖斗车厢的玻璃窗紧闭着，大伙儿不时往里探头探脑，里面的老 太太正在对着一份份临时居留证查找信件。一会儿，一扇窗子打开了，老太太大声叫一个个名字，把难民署寄来的复信或者是汇款单递出来。大家伸着手，把信件在头顶上传递着。传到朱手里的还是那张临时居 留证：今天没他老婆的信，过几天还得来看看。<br />
<br />
<br />
也有个别志向和一般人不同的温州非法移民，不愿意将政治避难 仅仅当做走过场。我以前听人提起过1990年左右到法国的神通广大的老 林，在国内老家时还是镇上的干部。他就花了大力气，希望攻克下法 国难民总署，这样，他就可以拿到法国的长期居留，无须像自己的同 乡那样在漫长的非法生活里苦熬，可以堂而皇之地开店、办工场，把 妻子儿女合法地办理来法国。政治避难是保密的，如果他愿意，可以 让除了他自己和法国难民署外，谁也不知道他是政治难民。过几年， 再去申请个法国籍，就可以一圆光宗耀祖的回乡梦。 他的申请信是请律师写的，文采璨然地说他曾经是个&quot;活跃分子&quot;，基层组织者。他在老家 遭到迫害和关押。令他的好友吃惊的是，他居然还弄来了不知真 假的国内公安局的逮捕令，又有一个颇有名气的流亡民运人士给他出 具了在北京&quot;并肩战斗&quot;的证明。下了大资本的结果是引起了法国难民 署的重视。老林被请去难民署专门陈述他的情况。老林在家里准备了 几天，把自己掌握的文字资料和录像带认真地复习了 一遍。在他把烂熟于心的故事充满感情色彩地复述一遍之后，难民署 的法国官员开了口，出乎意料地用地道的中国话和他交谈起来。<br />
<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;&quot;你在1989年6月4号前后一直住在北京吗？&quot;<br />
&nbsp; &nbsp;&nbsp;&nbsp;&quot;当然，要不然怎么能搞运动！&quot;<br />
&nbsp; &nbsp;&nbsp;&nbsp;&quot;那你天天都要去天安门喽？&quot;<br />
&nbsp; &nbsp;&nbsp;&nbsp;&quot;当然。我天天都要去天安门声援学生。&quot;<br />
&nbsp; &nbsp;&nbsp;&nbsp;&quot;那你在北京住哪里呢？&quot;<br />
&nbsp; &nbsp;&nbsp;&nbsp;&quot;……嗯，住北太平庄。&quot;老林想起有一年去北京出差，旅店就在 北太平庄。<br />
&nbsp; &nbsp;&nbsp;&nbsp;&quot;那你怎么去天安门？&quot;<br />
&nbsp; &nbsp;&nbsp;&nbsp;&quot;…… 坐公共汽车。&quot;<br />
&nbsp; &nbsp;&nbsp;&nbsp;&quot;坐几路公共汽车？&quot;<br />
<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;&quot;……&quot; 老林怎么也没有想到，有关&quot;遭受迫害&quot;的重大主题， 难民署的官员会去问这样简单的枝节问题。他原以为，凭自己多年当 基层干部获得的对中国的政治、行政的具体了解，说起这类事儿，这 些远在万里之外的法国佬只有睁大了眼睛，听他上课的分儿。他后悔没 把北京交通图、不同的票价、站与站之间的开车时间等等整个儿给背下 来，不过想想，做到这一点似乎又不太可能。花了大价钱弄来这些有 力的证据，结果也落得和那些只花了几百块钱、让人随便填了个表的 同乡一样。据说这之后，老林是好长时间抬不起头来。</div>

							
							
							
							
