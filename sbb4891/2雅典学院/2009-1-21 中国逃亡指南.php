<br />
第一条，跑路过程中的通讯<br />
<br />
<br />
1，最大的忌讳，用手机<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;手机虽然作为很先进的通讯工具，方便了联络，但是在跑路途中，这个确是最大的安全隐患，一个电话可能你就会被KO掉了。可能有的朋友会说，你说的是因为手机卡是用你身份证办的吧，你可以办那种不用身份证办的电话卡啊，或者是用别人的身份证办的卡啊。问题不在这里，而是目前的手机定位情况，如果真的是犯了比较大的事情，一般你的手机肯定会被锁定的，包括你的通话和短信。至于定位，一般开机40秒之内定位你没什么问题。可能又有不懂的童鞋要问了，我又没有打电话，怎么会定位我呢？不知道你们有没有用过Googlemap的定位功能，或者是AGPS,这个给手机没有GPS功能的朋友带来了很多方便，可以实时定位你的位置……，这个的实现其实是基于手机信号发射站来定位的，多维定位法，一般精确度在300-400米范围内，你所处位置的基站越多，定位越精确，只要你手机开机，有信号，基站就会不断扫描你手机信号源，然后多维定位。所以……跑路过程中最好是不要使用手机通讯，否则你很快就会被锁定……。可能你在美国大片中经常看到有人打电话然后被锁定在某个建筑内，这当然不是危言耸听。我不知道他们是不是基于手机基站扫描定位，我想可能还有卫星定位技术在里面吧，不管怎么样，手机还是不用为妙<br />
<br />
2，固定电话联系方式<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;很多人之所以被抓，可能就是因为实在想家，用固定电话给家里打了个电话。然后很快就被KO掉了。固定电话不具备移动性，一般打电话都会显示区号，然后根据这个区号，再去当地电信查，很快就能具体到某一不电话机。可能又有朋友要问了，我用街道旁边的IC卡公用电话，总找不到我吧，这个，实在的说，更好找到你的大致位置了，街边的IC卡公用电话都是分段号码，并且在电信里直接有分布图，根据你的电话号码，很快就能追踪到你所使用的那部电话所在的街道名称和位置……，这样你的位置还是会被暴露，给跑路带来风险。<br />
<br />
总结：<br />
<br />
跑路过程中的通讯解决方案<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;可以找到当地的中学生或者是去网吧找个，给他一些钱，让他打电话给另外个城市的朋友或者网友，当然，距离越远越好，然后通过他再转告给你想联系的人，如果为了更安全，可以多跳转几次。这样即使追查起来，想找到你的位置，可能在某个环节就会断掉。<br />
<br />
第二条，跑路过程中的交通<br />
<br />
&nbsp; &nbsp; 跑路，当然是要跑了，跑的越隐蔽越好，越少人知道越好，越快也越好。所以交通是很重要。<br />
&nbsp; &nbsp; 交通无非就是海陆空，机场车站，码头，或者自己开车或者驴车马车牛车都可以。没有做不到，只有想不到。<br />
<br />
&nbsp; &nbsp; 首先，坐飞机跑路这个是首先要排除掉的，机场人员众多，安全检查有是最严密的。警察叔叔安排的数量也是最多的。身份核查也是最严密的，身份证识别是和公安局的那个身份证识别系统是联网的，去机场坐飞机跑路等于自投罗网。聪明人一般都不会坐飞机跑路。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;其次，坐火车跑路，这个也是很不明智的选择。火车站现在安检级别也升级了，火车站内监控众多。虽然说火车站人多，置身其中还是相对安全的说，但是门口中总有警察叔叔在那里查身份证，看到可疑人员就会让你掏出身份证在电脑上查询一下（fuck,插播一条新闻，本人每次坐火车基本上都会被视为可疑对象查身份证），所以这个路线也不是很安全的，万一被警察叔叔查身份证，那当场就挂了。不推荐<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;关于坐汽车跑路，汽车又分长途和短途汽车，长途汽车一般发车目的明确，距离虽然远，一般都是走高速公路，但是可能就在几个地方停，所以万一暴露始发地点，目的地很快就会暴露，缩小警察叔叔的追捕范围。但是长途汽车暴露时间短，接触的人也少，这个可以适当考虑，当然，坐长途汽车，你可以不到达目的地下车，可以在某一路段下车，然后在路上拦一些跑长途的货车，搭个顺风车，有钱的话适当的给车主一些钱，他们还是乐意带你的，当然，这个要看人品和运气了。短途汽车，安检比较松一些，目的地短，经常换乘，迂回主动性比较强，但是由于暴露时间长，接触的人多，人多眼杂，也容易被发现，需慎重考虑……<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;另外一个走公路考虑的就是租车，找个人给他一些钱，去租赁公司租一辆车，走小路开，这样暴露的可能性就很小，安全系数高一些，这个是公路最佳出行方案……<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;走水路，全国跑路要走水路的地方貌似不多，内陆无非就是从重庆到珠江三角洲走水路，外围就是从广西，湛江，珠海，广州，青岛，南京，天津，海南，大连等城市坐船，不过这个危险系数高，非常，极度不推荐……<br />
<br />
第三，跑路中的身份伪造<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;跑路过程中一定不能用自己的真实身份证，这样很快就会被警察叔叔盯上的。在外面不管住店还是什么的，都是需要身份证的。至少要有三张或者三张以上身份证。至于身份证的来源，大致有两种。办证广告遍布全国各地，更有亚洲办证集团公司，每个城市的天桥下都有专门给人办证的，只要你想要的，没有办不出来的，50块钱一张身份证，200块钱办的比真的都还真，随便花点钱，都可以给你办出来一打。另外一个就是找你当地的贼头，这个需要一些关系，他们手头一般都有别人的身份证，花点钱，从他们手里买个两三张真正的身份证还不是什么问题的，当然，要找和你长的比较相近的，年龄差别不大的，不要连性别都搞混淆了。<br />
<br />
第四，跑路过程中的住<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;跑路绝对是一个长期的过程，在这里肯定要考虑到住。你不能一直在路上跑啊，总有累的时候，需要一个落脚点。住当然是短期的住，跑路不可能在一个地方呆的时间太长，除非是绝对的安全，当然绝对的安全是不存在的。首先住有几点方案推荐<br />
<br />
&nbsp; &nbsp; 1.对于经济条件和安全性来考虑，3星级以上的宾馆推荐住，因为这些地方一般都是比较正规的地方。一般不会有警察过去查房或者抓赌博嫖娼什么的，星级越高，安全系数越高，有些地方的五星级宾馆一般都是政府招待单位，除非有特殊情况，否则警察叔叔是不会贸然去查房的。另外一个就是当地政府招待所或者宾馆，这些地方，警察也是避之不及的。安全系数更高。不过要注意尽量避开监控设备，最好带着帽子什么的进去，用假身份证开房。<br />
<br />
&nbsp; &nbsp; 2。就是大一点的洗浴中心之类的娱乐场所，这些地方，一般都是有社会背景的人开的，越大，后台越硬。这些地方一般都提供免费住宿，因为是洗浴的地方，只有入口等处有监控设备，其他的地方不会有的，进去洗浴又不需要身份证，降低了安全风险，何况里面吃喝洗住玩一条龙服务，在里面呆上十天半个月，没人会管你的，不过建议还是不要常呆，否则引起怀疑，会带警察过来找你的。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;3.找一些农家乐，现在的很多农家乐都提供住宿服务，当然农家乐里面不会有监控什么的，管理上虽然登记但是和其他地方相比，还是要松懈的多，警察也一般不会没事跑那么远去查房，在里面住上三五天，还不是什么问题，何况农家乐一般都远离市区和农村，信息交流不是那么及时，安全性相对较高……<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;4，一些小旅馆之类的不推荐入住，因为这个会经常被警察叔叔查房，另外人多眼杂，安全系数极度不高，不过学校周围的小旅馆相对来说安全一些，因为毕竟里面都是开房的学生居多。警察叔叔也是心知肚明，除非是缺钱花了或者是学校联手想去查房，否则一般不会去的，可以考虑短期入住。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;5，最落魄最省钱的住，可以考虑和流浪汉或者拾荒者住在烂尾楼或者桥洞下面，一般城市都有烂尾楼，里面一般都聚集这流浪汉或者拾荒者。这里一般情况下警察是不会清查的，何况一般的烂尾楼外面都是大门紧锁。突然多出来一两个陌生人，他们也不会过多关注，只要你不和他抢地盘，他是不会找你麻烦的，也没人想招惹麻烦，所以在那里呆上十天半个月，相对来说还是比较安全的，这种方式推荐落魄期住……<br />
<br />
第五，关于隐<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;古人云，大隐隐于市。看来古人也明白，真正的高手都是隐藏在市井中的。所以我们也不可避免，遵循古训。目前来说，人员管理混乱的地方就是建筑工地之类的地方，因为建筑啊工地人员流动大，登记管理上也有很多薄弱环节，拿着假身份证出去登记一下，基本上就可以在里面干活了。大一点的建筑工地可能会在一个地方呆上一年多，然后再转战其他地方。警察叔叔不可能每天没事就往建筑工地上跑，至于工程项目组的他们最多也就是知道包工头的人数和名字，至于包工头下面有多少人，恐怕只有包工头自己清楚，也就是在开工和发薪水的时候清点一下人数。所以相对来说，还是比较安全的，另外一个工地上的人都是来自五湖四海的，没有谁会问起谁是干嘛的。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;切忌往农村跑，不要以为农村偏远就安全了，其实这个观念是错误的。至少来说，现在在偏远的农村，都是和外界有联系的，很多都是几辈子土生土长的原生居民。突然多了一个陌生人，这个都是他们茶余饭后的谈资。农村人爱扎堆聊天，时间长了这些消息会传的很远，很可能就会传到当地警察叔叔的耳朵了。地方再偏远，也是party的管辖范围，有村支部之类组织的说，很快就会暴露身份。<br />
<br />
&nbsp; &nbsp; 另外一个地方就是，矿山，林场之类的。矿山我就不多说了。云南，陕西，山西，河南，内蒙古，四川都有煤矿或者其他矿产，当然，最好的是隐于煤矿，这个一只脚踏进阎王殿的职业，内蒙古或者平顶山的露天煤矿就不要考虑了，这样隐不住，会露出狐狸尾巴的。最好就是往山西或者陕西跑，小煤矿众多，矿主不会把统计名单送到公安局的，除非他想关门大吉。人数报的越少越好，这样万一出了事故死人了，可以隐瞒死亡人数，减少麻烦。也正符合了你隐的条件，不过你要做好心理准备，不是什么非在这里隐不可的情况，就没必要来这里拿生命开玩笑，毕竟你跑路的目的也是为了活命或者其他的。小命都没了，还谈什么隐……<br />
林场也会招一些临时工人，这些也不会统计上报的公安机关的，短期的隐，在那里干个半年几个月的，不会有什么问题的。适合短期的隐……，这个适合往东北跑，大兴安岭那边适合隐，如果运气好，隐一辈子不是什么问题。<br />
