													<h2>野夫：畸人刘镇西</h2>
						
						
																											<div id="postmessage_801669" class="t_msgfont">一<br />
<br />
畸人，是伟大的庄子为汉语贡献的一个名词。他认为这样的人，“畸于人而侔于天”；也就是说他们在人世间孤独无匹，却与天道完美契合。<br />
<br />
我每每看见这个词，就想起故乡的莫逆之交老刘。在这个世界，我有幸结交过万千奇人，但是真正能当得起这个“畸人”称名的，似乎非他莫属。<br />
<br />
最近的一次还乡，我们又坐到了一起。朋友们问我――为什么还不写老刘？我还没来得及回答，老刘自己便解释――他说要等我死了才写的，可是我偏生是个老不死的，看来诸位还得等了。<br />
<br />
大家笑罢，我忽然内心涌出一丝歉疚和凄凉。难道我真的要到他坟前去焚稿，才能倾尽我们三十年的交谊吗？我何不趁他健在，就给他朗读我积年的知遇和敬重呢？<br />
<br />
是的，是朗读而不是给他看，他早已看不见我的文字了。他圆睁的双眼在这个世界始终像怒目金刚，可是却早已被黑暗遮蔽。他每天在孤老院里，拿着我的书去祈求那些识字的护理员读给他听，听着听着那枯井般的眼眶就泉涌两行。<br />
<br />
就是这样的一个睁眼瞎，每天却拒绝策杖；他独自像明眼人一样横行于闹市通衢，而且总要高唱着自己所谱的歌曲，旁若无人地行走在他的江湖生涯中。<br />
<br />
 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;二<br />
<br />
1977年我还在利川高中读书时，便认识了老刘。但是那时他不认识我，那时他在广场的戏台上被反绑着示众，然后我们这些集合去参会的学生，惊骇地看着他被宣判为现行反革命，被判刑八年，然后被恐怖呼啸的刑车带到了省城监狱。<br />
<br />
1981年我大学毕业回到小城，浮躁孟浪地成为了一个薄有姓名的诗人。一日，我和文化局长刘湘松在书店闲转，正在翻看新到的《中国古典十大悲剧》。忽然一个带着高度近视眼镜的中年男人急匆匆闯来，近乎无礼地直接从我们手中夺过该书开始翻阅。他一边看目录，一边嘀咕――牡丹亭怎么也收进了悲剧？<br />
<br />
刘湘松是儒雅富学的人，便搭腔道――这个在学界也有争议，可算悲喜剧吧。他一听我们的议论接近内行，立马回眸打量问道――两位高姓大名啊？我们眼见此人古怪，寒暄着自报家门；他突然双手各自紧拽我俩，高声笑道――神交已久，缘悭一面。未曾想今日邂逅，请两位务必到寒舍小坐。<br />
<br />
我们赶紧模拟他的古旧声腔，回问阁下怎么称呼；他爽朗答曰在下刘镇西便是。我们都记起了这个小城著名的政治犯，想到初识不便登门，便委婉客气曰改日拜访云云。哪知他完全不由分说，直接强拉着我们跟他进入一个歪斜的木楼。<br />
<br />
上得楼来，他家却柴门深锁，我们又急忙托故说下次下次。他哪里肯听，直接将我们按进邻居的椅子，说他去去便来。只听他在院子里喊了几声老妻，便去敲隔壁一家的门借斧头。我们像遇见强人打劫一般，急忙出去拉住他说，千万别如此，我们改日一定再来。他已然利器在手，口中念念有词曰――幸有嘉宾至，何妨破门入。手起刀落，门锁已被他砍成两截。就这样，我们在他不足十平米的暗室，杯茶订交，成了今生头颅相许的朋友。<br />
<br />
 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;三<br />
<br />
老刘似乎是一个始终活在古代的人。他无论言谈举止，处处都透着古风。上面那些对话，外人以为我乃虚构明清小说的口吻，殊不知利川的百姓，但凡接近过老刘的人，皆知我所言非虚。<br />
<br />
那时的他约略四十多岁，皮肤漆黑，额上皱纹深刻如横写的川字。浓密的眉毛几乎要连接在一起，下面是一副宽边的五千度的眼镜。他自嘲说根据古代相术，他就是天生的苦相。我看他的形貌，不需要懂麻衣柳庄，那也是绝对可以看得出的悲苦。<br />
<br />
那时他刚刚平反提前出狱，原本没有工作，故而也不存在补偿工资一说，三年多的深牢大狱那算是白坐了。问起案由，则才知道其中的荒诞。原来他早在1958年，就因同情右派乱说反动言论，曾经被劳教过三年。两劳人员在中国，属于地富反坏右之中的坏分子一角，属于要永远监督惩罚的对象。<br />
<br />
但凡国家有大事，基层政权都要集中这些五类分子学习训话，观察反应。1976年的打倒四人帮，自然是审看这些所谓反动派的最好时机。他被叫到了城关镇政府，问他对这一事件的看法。老刘一生耿介磊落，反问主官是要听真话还是假话。主官一向反感其桀骜不驯，自然窃喜说要听真话。他说――那你等我回去把后事安排一下，马上就来回答。<br />
<br />
老刘回家收拾一床薄被【那时坐牢是要自带被子的】，妻问他干嘛，他说明缘由――要去镇上讲真话，肯定就会坐牢。他把妻女托付给一家朋友看顾，妻是文盲，抱着他的腿哭泣不放，哀求他不要去管什么国家的闲事。他是那种绝不屈服和畏惧的人，还是悲风扑面地走向了衙门。<br />
<br />
他对那些主官慷慨激昂地说――四人帮固然是极左，但华国锋也是极左出身。但凡了解他在湘潭地委书记任上的作为，以及文革中的火箭突起，就可以断定这次宫廷政变不过是窝里斗。历史最终将证明，他只是一个过路桥，还有人将踏着他过河并最后拆桥的。<br />
<br />
此番高论在当时自然石破天惊，很快便获刑八年。求仁得仁，于他而言不过是换一个碗吃饭。但是历史却很快证明了他的预言，于是他得以提前解脱桎梏。问题是在中国，这样的人还能活下来，多数则是要谢皇恩浩荡的，哪里有国家赔偿一说。他只好重操旧业，靠在搪瓷碗盆上烧字养活妻女。<br />
<br />
 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; 四<br />
<br />
烧字这一手艺，在今天已然绝迹。那时各个单位学校食堂，多是使用统一的搪瓷碗盆，为了防止被人偷窃，往往便要烧上某某食堂几个字做记号。学校毕业生或军队转业者，也都喜欢发一个某某纪念的碗盆。于是，操此手艺者就能勉强求食。<br />
<br />
烧字的工序是先用一种红色的瓷粉矿物质，在器皿上书法，然后再用高压煤油喷灯，像氧焊一样把那些字融进器皿，冷却之后就再也洗刷不掉了。但是一个单位烧过了，基本永远不会再有需要。也因此这一手艺者，注定要在各地流浪乞食。我认识老刘的时候，他就是这样背着一个简单的木头工具箱，走遍了二十几个省的无数县镇的。<br />
<br />
他和那些江湖手艺人唯一的不同是，他的工具箱里永远放着《楚辞》。那些异乡的青灯雨夜，屈子的骚赋一直伴随着他的自我放逐。没有人相信这个衣衫落拓的苦命人，竟然是楚辞的横流倒背者，且更是楚辞古韵和名物的民间研究者。<br />
<br />
他的生涯便是这样越走越远的，赚来一点钱，路上便邮寄给妻子，自己只留下到下一站的车票。每半年左右回来一次，休息十天半月就又要上路。每次倦游归来，首先便要到我处小坐，谈谈在路上的故事，以及沿途见闻的国家走向。<br />
<br />
他比我大二十几岁，萧条异代，我们却成了山城最密切的忘年之交。那时的小城多雨而寂寥，冬天往往深雪覆盖。他在许多个夜晚踏雪而来，在我的斗室围炉长话；我第一次看见一个江湖老男人，读完我的《致毛泽东》而掩面恸哭。他的青春被毛时代毁灭殆尽，内心的苦楚无人可诉，我则是他唯一信托的兄弟。<br />
<br />
我也常去他的陋室看他，他是著名的围棋迷，在我们那边远小城，那时熟稔这种玩意的不多几人。因此全县的棋赛，他也往往可以入围前三。有次我去，看见他正和一位老师手谈。他的妻子一会过来对他耳语――中午没米下锅了，要他去找人借米。他入棋正深，挥手不语。妻子眼看断炊，又来高声催促，希望对手封盘，下午再战。对手尴尬起身，却被老刘一把按住，只好接着迎战。其妻再也按捺不住，一把掀翻棋盘；老刘恼羞成怒，几乎要动手，终也还是被我劝住。我才知道他家的日子，竟然窘迫至此。<br />
<br />
次日，老刘来我这羞涩地借钱，他说只要五块钱，够他上路的车票即可。我要多给，他却坚持不要。我只好将吃不完的粮票塞给了他，看他摸索着远行他乡。<br />
<br />
 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;五<br />
<br />
老刘的妻子叫桂枝，我们没大没小地也就直叫老嫂子。嫂子也是那种苦相的人，没有工作，独自带着一个上小学的女儿，每天依门守候着老刘的挂号信――那是他们母女赖以存活的唯一指望。<br />
<br />
偶尔我会看见嫂子在街边嗑瓜子，年轻的我好恶分明，想到老刘终年流浪的辛苦和谋生的艰难，便有些郁郁不乐，觉得她不该这样好吃零食。<br />
<br />
一次老刘归来，沉重地找我诉说――想要离婚。说这个女人太不理解他了，唯一的一点读书下棋爱好都要被剥夺。那时的我真是不谙世事，立马就表示支持，而且说这个女人太拖累他，希望他早日解脱。<br />
<br />
老刘似乎找到了他最在乎的动力，仿佛下定了决心一般出门而去。几天之后，他又颓丧地来了，我以为他已经办完手续。哪知他沉痛地对我说――我不离了。我询之，他这才告诉我，关于他的婚姻与爱情、、、、、、<br />
<br />
老刘十几岁就被劳教，出来之后带着坏分子的帽子，自然没有人敢嫁他。那时的他靠当泥瓦工在建筑队糊口，整个青春期完全与女人无缘。到了1975年左右，终于有媒人说合，给他介绍了现在的妻子。媒人只说是乡下农妇，守寡带着一个女孩，且女孩还是婆家在喂养。老刘正当年，却又寒促逼人，如何可以挑剔对方的条件，两人很快就结合了。<br />
<br />
婚后未几，妻子告诉他自己已经结扎，再也不能生育。女儿是送给别家的，病了需要救治。老刘虽然有些憋屈，但立刻就答应把女儿接回来喂养了。就这样，一个弱小的生命，被老刘抚育到那时。<br />
<br />
老刘泪眼朦胧地对我说――我不能离，离了后这个孩子就失去了生活，肯定不是死就是被卖出去。她虽然不是我的亲生孩子，可是我把她养大，就是养个猫狗，那也养出了感情。再说这孩子心地善良，对我如亲父，我岂能看着她被遗弃啊。<br />
<br />
当我知道这一深层原因之后，顿觉脸红；在老刘的善良高尚面前，我愧觉了自己的少不更事。他们夫妻此后再也没有什么纷争了，但是哀伤的故事却远远不止这一些。<br />
<br />
 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;六<br />
<br />
我在这个国家，见过无数悲苦的女人。但是像老刘妻子这样的悲剧人物，我还真没见过超乎其上的苦命。有时不免想，这是怎样的一种因缘啊，天地之间竟然把这样两个极端苦难的男女组合在一起，仿佛就是要为这个邪恶的时代立此存照。<br />
<br />
老刘新婚未久，某天回家，突然看见高矮一顺溜多出了另外三个孩子。每个孩子都面黄肌瘦，嗷嗷待哺的样子，惊恐地看着他。妻子正在打骂那些孩子，看见他出现也一时手足无措，紧张地不知如何说起，只知道泪流满面。他把妻子拉到一边细问，这才捅开一个天大的秘密。<br />
<br />
原来桂枝在嫁给他以前，已经有过三次婚史和四个儿女。前面的三个男人，一个病逝，一个坐牢离婚，一个车祸身亡。她一个无助的女人，怎么也无法独自抚养四个孩子，况乎还在那样一个农村极端贫苦的年代。因此，寻求再嫁一个城里人，找一份供应粮，是她唯一求生的可能。于是，她把那几个稍大一点的孩子，各自托付给他们不同的爷爷奶奶家，自己则在媒婆的隐瞒下，找到了老刘这样一个忠厚人。<br />
<br />
那个年代到处打零工的老刘，无论怎样善良，实际上都无法养活老婆和四个孩子。桂枝也深知这一点，不愿太拖累他，只敢带回最小的女儿，而继续隐瞒着前面的婚事和孩子。哪知道十多岁的大儿子，听说母亲嫁到了城里，知道弟妹们在各家都在挨饿，便相约一起进城寻母，只为要吃一顿饱饭。<br />
<br />
这群半大不小的孩子自个打听，竟然饥肠辘辘地找到了母亲。贫穷的母亲看着瘦骨嶙峋的孩子们，垂泪不已，但是她实在没有能力和勇气抚养他们，也不敢让老刘知道她还有如此沉重的负担和卑微的历史。只好赶紧让他们吃一顿饱饭之后，催促他们回去。孩子们也渴望母爱，恋恋不舍磨蹭着想留下，想和小妹妹一样有人疼爱。母亲万般无奈流泪打骂他们，要他们赶紧趁天黑之前回家。这时，老刘疲惫地回来了。<br />
<br />
突然知道这些情况，我想对多数人来说，都是一个考验――毕竟一个女人似乎隐瞒了太多的往事，且无端添出了这么多的责任。老刘听罢妻子哭诉，二话不说，带着这群孩子就进了一个餐馆，他将当天收入的钱全部拿出，为孩子们点了一大盆红烧肉，看着他们饱餐。然后对他们说，他实在养不活全家，甚至唯一的一间房也住不下大家。孩子们还是得回到各自的爷爷奶奶家去，但是每月可以进城来打一顿牙祭。<br />
<br />
 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;七<br />
<br />
我在1980年代与老刘时相过往之时，他的日子稍微好过一些，养女已经在小学。他一直是利川的文化人之一，爱写诗歌，新旧体都写，与我也不时唱和。他还爱谱曲，偶尔发表在一些基层刊物上，便也其乐陶陶。更不可思议的是，他一直在默默地研究楚辞的古韵和名物。他的足迹主要活动在古代的楚文化地域，因此他有心在各地方言中去求证古音韵的残留现象，以及楚辞中的大量植物名称与现在楚地的存活植物的对应关系。<br />
<br />
音韵学一直是中文系称为“绝学”的学问，我在第一个大学时，古汉语老师讲到音韵学时，便明确说自己不懂，大家自学粗通即可。老刘这样一个从未上过大学的民间爱好者，且又时刻处在乱离岁月中，与学界毫无联系，他怎么要迷恋这样一种孤僻的学问呢？为了成全他的爱好，我还是送了他不少楚辞研究的书籍。<br />
<br />
悲剧还是要上演了。某日他兴冲冲地找来，从怀里掏出一卷书稿对我自得地说――我终于完成这本书了，半生的研究总算有个结果。我拿过来一看，原来是楚辞韵读的手写稿。也就是说，楚辞按今天的普通话读，很多已经不押韵，但是在古代，它是押韵的。它在古代究竟是怎样的读音，老刘给你一一标注出来――这就叫上古音韵学和方言研究。<br />
<br />
我翻看了一下他的稿子，心中犹豫再三，不忍破坏他的快乐；但是最终又不能不告诉他真相。我从书架上抽出我刚买的王力先生《楚辞韵读》和《诗经韵读》给他，对他说――老刘，你晚了一步。老刘急忙打开翻阅，一会只见眼泪滴答在书页上，最后竟然伏在我膝盖上嚎啕起来。<br />
<br />
一个民间学人，没有基本的生活保障，更没有学术信息，完全不知道学界的发展状态；他就像一个暗夜的瞎子一样，完全靠自己摸索前进。钱钟书先生曾说，意大利有一个典故成语叫――发明伞的人。老刘实际上就成了这样的一个人。此后他焚稿断痴，再也不谈楚辞了。只有在极少的朋友圈子谈起某个乡间植物时，他会指出，这就是楚辞中的某某。<br />
<br />
 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;八<br />
<br />
一个两劳释放人员，一个高度近视接近盲眼的人，没有固定工作，更没有社会福利，还要加上拖家带口，其日子无论如何也不可能好转更多。更不要说老刘的性格耿直孤介，不善与人相处了。<br />
<br />
一日在小街上，我远远看见他岔开双脚，举步维艰地移动向前；急忙过去扶住他问。他痛苦摇头叹息说，他不幸染上了“坐板疮”，屁股长满脓疮，不能出门谋生，只好在家养病。我问他去医院看看没有，他说哪里有钱看病，就靠自己每天热水烫洗，也许慢慢就好了。<br />
<br />
这次我是第一次对他发火了，我大声呵斥他为何不来找我，他说欠我太多，不好意思再添麻烦了。我说你不知道我是公费医疗吗？我带你去医院以我的名义看病开药，我都是可以报销的，你难道还要为这个鸡毛国家省钱吗？你不赶紧治病出门谋生，你一家子怎么活啊？他说已经借了不少人的米了。我愤怒而哀伤地拉着他直奔医院，打针开药，这么点小感染，对西医来说不费吹灰之力。<br />
<br />
他很快好了，又来嗫嚅着借钱上路，总是三五块，反正他每次回家是首先必来还钱的。但是他的生意却是越来越难做了。80年代的改革开放，各单位再也不把盆盆钵钵当一回事了，因此烧字做记号或发放做纪念的，就越来越少。20世纪的突飞猛进，已经残酷地淘汰了太多古老的手艺人，老刘这样的畸零者，面对着时代张皇失措，实在想不出怎样才能跟进别人的脚步。<br />
<br />
但他是一生难得低头的人，多年的江湖游历，使得他生命力极强。养家活口――这是男人的使命，他必须硬扛着生活寻求变局。他决定放下他那老旧的工具箱，且很快学会了自己配制老鼠药。他每周一天驱赶出妻女，自己闭户锁门调配那些剧毒品，几乎成了东邪西毒一样的世外高人；然后再去各个乡间赶集出售。感于他的身世况味，我尝赠诗云――垂老街头作药师，一生偃蹇为诗痴。古时君子时终悖，当世高人世莫知。天性淳真不苟俗，秉心清苦已忘机。每回相对还相哭，寒士风姿让我思。<br />
<br />
我看他生意清淡，决定帮他义卖一次。那时我在县委工作，满街都是熟人朋友。我让他站一边收钱，我拿着半导体喇叭帮他守着摊子叫卖。过往人群见我卖药，都觉滑稽好笑，一时围观看热闹者甚众。我逮住每一个熟人要求他们必须买，有朋友哀求家里无鼠，买去实在没用。我说不管那些，买去两口子自己吃也行，反正掏钱才能走人。<br />
<br />
那天算是帮老刘挣了一笔，但是，这终究不过相濡以沫而已。<br />
<br />
 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;九<br />
<br />
武大毕业，我要南下海口了。回乡揖别亲友，老刘在街上拦住我说――我受了你多年的恩，却没请你吃过一餐饭。此次你远行，也不知再见之日。你嫂子桂枝说，无论如何要请你去家里喝一杯。<br />
<br />
我深知他家窘境，婉拒说你我之间，无需这些俗礼。他像抓贼一样抓住我哽咽说，我也请不起更多的人作陪，就请了你的至交苏家桥，你们俩要是不肯给我这个薄面，那我们今生也就到此为止了。<br />
<br />
晚上我只好带着苏家桥去了，桌子上果然没有多的菜，两荤一素一汤，却只放着两副碗筷和酒杯。我诧异，他喃喃云他们全家都先吃了，就想看着我们喝酒聊天，他们一家便足矣。我怎么也要拉他们上席，但是连初中的女儿都坚拒不肯。<br />
<br />
我和苏家桥只好坐下开饮，世间这样的请客法，平生我也就看见这一次。酒到微醺，老刘取出他那一把二胡说――我为送你远行，填了一首词并谱曲，且教会了桂枝和女儿。现在聊助两位酒兴，我们全家一起为你们合唱一下。唱得不好，万勿笑话。<br />
<br />
就在那空空如也的陋室，苍凉的二胡声开始回旋。他沙哑的嗓子，和着他五音不全的文盲妻子的如泣如诉，再加上一个少女脆生生的童音，像三重奏一样唱起来，且歌词又是他的妻女尚无法全懂的文言。我和苏家桥再也无法忍住我们的眼泪，他们就那样投入地缠绵回环地长歌，我们就这样涕泗交流地低泣。连初初懂事的孩子，都唱出了眼泪，这是怎样朴素苦情的一家啊。<br />
<br />
那一夜的别情，至今想来还是酸涩。之后，我果然差点真就是相见无日了。<br />
<br />
 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;十<br />
<br />
我坐牢那几年，每年冬天都要收到老刘寄来的一双老棉鞋。那是老嫂子桂枝一针一线手扎的，温暖结实。武汉的冬天凄冷难言，那些来自山中故人的暖意从脚底升起，使我今生都能笑对尘世炎凉。<br />
<br />
刚一释放，老刘就来信说要来看我。间关千里，我不忍要他奔波，但他还是摸到了我那脏乱差的出租屋。这时，我才知道他的眼睛已经基本失去视力了。他拿着我在狱中写的诗集，完全是鼻子顶在纸面上，才能勉强分辨阅读。我劝他不要读了，他坚持要读，他从中读出了我和他两代人共同的那些经历和记忆，他不时狂笑如疯子，不时嚎啕似孩儿。<br />
<br />
他的老鼠药因为太有效，国家不许民间配制剧毒，他又失去了生计。但是这样的人注定是天地难杀的人杰，他又改行做起了肥猪增长剂的生意，依旧是瞎眼去赶周边的乡场勉强活命。这时，他的养女已经辍学，小小年纪就到福建沿海打工去了。<br />
<br />
那时正是我也走投无路之日，无从帮他，大家布衣相交一场，还得各奔生路。哪知当年底，我为彻底轻身远行，回乡要去为外婆拾骨迁坟，又只有找他帮我出力了。故乡是土葬，偌大的坟堆和沉重的棺材，十年入土的尸身究竟何般模样，这都是我独自无力料理的难事。老刘带着工具和几个晚辈大早随我上山，在乱坟岗上找到我外婆的碑刻，他说――你跪拜完就到一边去等着，这个时候你是难以面对的，就让我代你为婆婆尽孝吧。<br />
<br />
他瞎着老眼在那里一点一点地刨土，生怕那些晚辈挖烂了外婆的棺材。最后启开棺盖，我们一起细细地将外婆的骨殖一寸一寸地捡起来，他和我一起扛着已然不到十斤的骨头下山。我们再一次挥泪而别，皆不知未来还有何等厄运在等着。<br />
<br />
 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; 十一<br />
<br />
老刘70年代入狱之后，骤然再度失去生活来源的桂枝和女儿，岂是朋友真能彻底照管的。杯水车薪不足以解救艰危时日，更不要说政治上的牵连之虑了。<br />
<br />
迫于无奈的桂枝，再次被人介绍到了鄂东的乡下，带着孩子跟一个男人勉强度日。她没有和老刘离婚，心中依旧惦记着这个倔强而善良的男人。老刘出狱之后，人去楼空，他四处打探妻女的下落。没有这个女人，他在这个世界那是真的连家的感觉都没了。桂枝那边也一直在关注故乡的消息，她终于等到了寻找而来的老刘。贫贱夫妻的劫后重逢，大悲大喜都只换成了清泪两行。那个同居的男人并未为难他们，他们终于又破镜重圆了。<br />
<br />
这个有过五个男人的悲剧女人，在90年代似乎才开始过上一点安稳的生活。这时，她的其他几个孩子，都已经长大成人，分别工作成家。最小的女儿初中毕业，没能考上高中，跟人远去福建打工，每月给他们寄回一些钱来。老刘真是没有白疼这些个孩子，现在渐渐失去谋生能力的他，终于可以得到孩子们的反哺了。<br />
<br />
他们依旧节衣缩食地在底层挣扎。肥猪药的市场被四川刘氏集团垄断之后，老刘的生意也每况愈下了。那年初有改观的我，春节前从北京还乡去看他。嫂子坐在没有生火的屋里瑟瑟发抖，我知道故乡的严寒，问她为何不生火，她说没钱买煤。我问老刘呢，她说上街去写春联卖去了。<br />
<br />
我赶到街头，远远看见老刘摆着一个简陋的案子，在那里几乎鼻子贴着红纸，用毛笔书法着给万户千家的吉祥话。他的清涕就在刺骨的风中悬挂着，不时要垂落到纸面。我急忙过去夺过他的毛笔，我说你歇着，我来帮你写。他惊喜地搓手顿足，在一边乐呵呵地看着。<br />
<br />
他对我欣慰地说，孩子们都有孝心，他们已经攒下了几千元，终于买了一个破房子，现在正在简单修理，明年就可以搬进自己的屋了。他们一生都是在廉租房里度过的，我能想象他那种终于有家的快乐。<br />
<br />
嫂子也渐有老相了，我终于看见了她展眉一笑的容颜。看见这对苦难夫妻，似乎终于熬到了头，我也就略略安心了。谁知道次年突然传来消息――嫂子失足摔死了。我急忙去电详问，原来两口子修好那破屋之后，前去验收，嫂子在二楼一脚踏空，当场就断气了。还没搬进新家享受一日之福，就这样撒手人寰。命运于她，实在是太过薄幸了。<br />
<br />
 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; 十二<br />
<br />
老刘像庄子一样鼓盆而歌，送走患难相依几十年的荆妻，自己也骤临老境了。他一生酷爱的读书写字，因为眼睛几乎完全失明而不得不舍下。女儿每月给他寄一点生活费，基本能保证他的饱暖；但是做饭洗衣这样的事情，他在他的长夜里却实在难以自理了。<br />
<br />
恰好我的一个同学这时当上了民政局长，我给她电话说，利川是对不起老刘的。这样一个民间文化人，无缘无故两陷冤狱。而今失明的孤老一个，你们福利院不救助这样的人，那实在不近人情。同学亦善士，很快帮他解决了这个问题；哪知他却梗犟不愿去吃这嗟来之食。我只好给他电话，我说人要服老，没有一个朋友可以永远伺候你。你的养女已经很孝顺，但是她在外也不能照管你太多，她也还要开始自己的生活。这样老刘才搬进了福利院。<br />
<br />
福利院的住客多是文盲残疾孤老，无人可与交流，自然愁煞老刘。他的女儿安家在西双版纳，夫妻做熟食维持生计。刚好我亦在大理栖居，老刘决定暮年滇游，来看望我以及他一生都视同己出的女儿。我担心他形同盲翁，如何完成这数千里往返。他笑答平生遍历江湖，沿途自会找到相助者。某日凌晨，我接到一陌生电话，要我去车站接他。赶去果见一对父女陪护着风中的老刘，要亲手将他转交给我才放心而去。<br />
<br />
在大理，老刘与我一如荒江野老，在夜雨寒窗之下检点平生往事，遥远的伤痛再次令他泪满青衿――老刘的童年是在抗战烽烟下的奉节度过的，后来母亲去世，十来岁的他竟然独自徒步来到利川寻父。父亲是民国利川粮站的一个吏员，1949之后作为伪职人员饱受打压。他在50年代初考上农校，却为莫名其妙的一件小事被开除。反右倾时又因为言论忤逆而被强行劳教，等他释放回来时，父亲已经在大饥饿中奄奄一息。他为父亲找来了一点食物，结果饿得太久而狠狠饱餐的父亲，却被胀死了。<br />
<br />
他的一生几乎从来没有摆脱过贫困，底层人民的所有苦难他都亲历遍尝。我从未看见过他有任何自怨自艾的时候，他始终乐观地面对一切厄运。我很少看见比他还耐活的男人，他说他一定要亲眼见证这个罪恶时代的消亡。<br />
<br />
而今，我和他都在等着。他每天长歌穿过闹市，在世人的眼里像一个疯子一样的自得其乐。只有我深知，他悲苦的内心有着怎样的痛与恨；在他终年的黑暗里，是在怎样地渴望重见天日那一刻。 <br />
