<br />
谁了解新疆？谁了解新疆的维族人？谁了解当地的情况？<br />
<br />
其实很简单，新疆谁没有同学？新疆谁没有朋友？哪怕拐几个弯都会有了解的渠道和手段，可小组某些人只是猜想，只是臆想，只是主观的判断，你呀，真正不了解新疆这个地方和这个地方的每一个民族同胞！<br />
<br />
首先说当地的民族族别，不仅仅是官方的十三个少数民族，其实不止，仅仅是因为这十三个民族人数多而已，而且就官方数据，维族是目前新疆最多的民族，超过汉族，达2000万以上；<br />
<br />
其次，关于所谓维族在新疆受到迫害的臆想；其实今天都是网络社会了，虽然有GFW封锁，可是能封锁住人的嘴吗？能封住手机信息吗？虽然75之后新疆采取过手段封网封手机信息，但是过去乃至今天依然网可以上，手机可以发信息；了解一下嘛！<br />
<br />
