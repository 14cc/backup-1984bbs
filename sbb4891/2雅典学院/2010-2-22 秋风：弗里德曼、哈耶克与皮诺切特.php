													<h2>秋风：弗里德曼、哈耶克与皮诺切特</h2>
						
						
																											<div id="postmessage_487910" class="t_msgfont">皮诺切特介绍：刚刚去世的皮诺切特可以说是智利近二十年历史中一位至关重要的人物，他领导了战后拉丁美洲最残酷的一次军事政变，这次政变被舆论认为是改变了一个国家、甚至整个南美大陆的历史，皮诺切特在任期内还创造了令人羡慕不已的智利经济奇迹。对于他的功过是非，政坛各界褒贬不一。自幼立志做一名将军的皮诺切特18岁考入圣地亚哥陆军学院，此后在智利军界平步青云。1973年8月，他被当时的总统阿连德提拔为陆军司令，这为他日后登上国家权利的顶峰奠下了基石，成为陆军司令仅一个月后，皮诺切特在海、空军支持下发动了军事政变，推翻了阿连德政府。皮诺切特上台后，采取的是一种高压、独裁政策，他解散国会并禁止所有的反对党的活动，在全国长期实行宵禁。智利的秘密警察部队不仅残害大批本国左派和爱国人士，还绑架、暗杀了成千上万的外国移民。据非官方统计，在1974年至1990年期间，智利共有41359人遭逮捕，3197人失踪或被杀害，10余万人被迫流亡国外。但另一方面，皮诺切特的铁腕统治也给智利带来了相对的稳定和发展。皮诺切特行武出身，并非经济里手，但他知人善任，特意找来了一百位经济专家，组成了顾问班子，制定经济复兴计划，实行开放型自由市场经济政策，很快扭转了困难局面。近二十年来，智利经济的快速增长在拉美一枝独秀，被国际经济学家誉为经济奇迹。1989年，皮诺切特在告别政坛前夕发表国情咨文时，感慨而又骄傲的向世界宣布：我接手的是一个满目疮痍的国家，而移交的则是一个具有坚实基础、旺盛未来的国家。皮诺切特在执政期间也为自己的命运作好了种种安排。在1980年军政府制定的新宪法中明文规定：皮诺切特总统如在1989年大选中失败，他将继续担任陆军总司令之职，即使从这一职位卸去也将立即宣誓就任终身参议员。有了这道护身符，皮诺切特就终身受到宪法保护而免遭任何起诉。1998年3月10日，在智利首都圣地亚哥军权交接仪式上，当皮诺切特把象征智利陆军最高指挥权的权杖和佩剑交给继任伊苏列塔中将时，国内舆论认为，这标志着智利一个时代的结束，具有特殊的历史意义。但就在他刚继任的头一年里，却又发生了一连串令这位智利前总统始料未及的、并将改变他命运的突然变故。此后，皮诺切特命运坎坷，颠簸流离，直至去世。<br />
