2004年02月21日 06:42 深圳商报 <br />
弗里德曼、哈耶克与皮诺切特 <br />
<br />
<br />
2004年02月21日 06:42 深圳商报 <br />
<br />
<br />
去年秋假，笔者应邀参加在北京航天大学西门外乌有之乡书吧举行的一个学术活动。那位人士说，主题是讨论哈耶克。作为《哈耶克传》的译者，我对于任何有关哈耶克的活动都有兴趣。<br />
<br />
确实非常有趣。活动一开始，主题发言就让我大吃一惊，题目是《哈耶克批判：自由主义与法西斯主义之间的转换》。演讲者所说的自由主义并非所有的自由主义，而是专门指在二十世纪中以哈耶克、米瑟斯、弗里德曼等人为代表的自由主义。他说，这种自由主义和法西斯主义可以很容易实现转换。在他的论证过程中，他举了一个例子，谈到了哈耶克、弗里德曼与皮诺切特的关系。他说，皮诺切特推翻民选的社会党总统阿连德，搞了他的一套经济政策，那么是谁去当他的经济顾问？是米塞斯和哈耶克的好朋友，芝加哥学派的领军人物弗里德曼。<br />
<br />
我虽然不喜欢这个发言提出问题的方式，但对于讨论弗里德曼和哈耶克，不管是两位学者与皮诺切特的私人关系，还是他们的理论与皮诺切特的政策间的关系，都是一个不能回避的问题。在《两个幸运的人：弗里德曼回忆录》中，弗里德曼专门有一章谈到了这个引起广泛争议的敏感问题，即第24章《智利》。<br />
<br />
弗里德曼与“恶魔”<br />
<br />
皮诺切特为了结束当时的混乱状态，发动政变，推翻了一个民选总统；在经过短暂的经济政策徘徊之后，便开始奉行一种以市场化为导向的政策：结果是令人惊讶的，自1975年以来以至今天，智利的经济是整个拉美地区最稳定的，也最有活力的。<br />
<br />
在这中间，一群被成为“芝加哥男孩”的年轻经济学家发挥了重要作用。这些年轻人曾在1955―1964年间到芝加哥大学经济学系受过训练，而弗里德曼正好任教于该系，他的观念不可能不影响这群智利学生。因此，尽管这些学生并不是弗里德曼直接教诲出来的，但他们确实接受的是弗里德曼的自由市场观念。他也亲笔签字认可过这些年轻人给掌权后的皮诺切特提出的医治当时经济难题的处方：休克疗法，借此结束通货膨胀；然后是削减政府开支。在这之后，市场将会发挥作用，创造经济繁荣。<br />
<br />
在讲究政治正确的知识分子、尤其是学院中，皮诺切特一直被视为一个恶魔，所以，弗里德曼与皮诺切特的瓜葛，也一直被视为弗里德曼的一大污点，他也因此成为诺贝尔奖历史上最大一次示威抗议活动所针对的对象。对于种种批评，弗里德曼在这本回忆录中似乎并没有给予有力的批驳，他只是申辩，自己与皮诺切特、与那群“芝加哥男孩”并没有太密切、太直接的关系。<br />
<br />
不过，在1982年1月在《新闻周刊》的一篇专栏文章《自由市场经济与将军》一文中，弗里德曼比较全面地论述了这个有争议的问题：一个威权主义政府，是否能够成为在某个特定时期推动自由市场制度建立和完善、实现经济繁荣的更有效率的手段？<br />
<br />
其实，这个问题，从东亚模式一提出，就引起广泛关注。1997－98年的东南亚金融之前，中西学者普遍对这个问题作出肯定回答；自那之后，则倾向于作出否定性回答。<br />
<br />
弗里德曼当然不会低估自由市场的政治影响。他那本1962年出版的名著《资本主义与自由》，第一章就是讨论《经济自由与政治自由的关系》。他写道：“经济安排在促进自由社会方面起着双重作用。一方面，经济安排中的自由本身在广泛的意义上可以被理解是自由的一个组成部分，所以，经济自由本身是一个目的。其次，经济自由也是达到政治自由的一个不可缺少的手段。”（中译本，商务印书馆，1999年，第9页）<br />
<br />
但是，经济自由与政治自由之间并不存在决定论意义上的那种关系。有可能存在这样的情形：政治上是自由的，但经济上却不自由，或不完全自由，比如50年代以后一直到90年代改革之前的印度，甚至包括二战后的英国。也有可能存在另一种情形：政治上不自由，是威权体制，甚至是专制体制，但经济却是大体自由的，皮诺切特统治下的智利大体就属于这一类。<br />
<br />
“令人惊讶的政治奇迹”<br />
<br />
似乎是预先对他对皮诺切特政府的立场提出一个论证，在这一章中，弗里德曼说：法西斯的意大利、西班牙、德国、日本，还有第一次世界大战前的沙皇俄国，这些都不可能被称为政治上自由的国家。但在这些社会中，“私有企业是经济结构的主要形式”。因而，私人产权也得到政府的保护。因而，在除了政治之外的大量经济、社会事务领域中，民众拥有相当程度上的自由。<br />
<br />
而且，这种经济自由有利于“保持政治自由”（《资本主义与自由》，第17页）。但是，弗里德曼并没有说，经济自由可以无中生有，在其本身之外，自动地创造出政治自由。这并不是咬文嚼字。在讨论东南亚的“威权主义＋自由市场”的模式时，这是各方争论的关键问题：人民是否应当接受现实，为了经济上的繁荣，而忍受政治上的威权体制，因为，假定政治上的自由可以随着经济自由自动降临，那么，忍受的代价或许并不是很大，甚至可能是必要的。<br />
<br />
但弗里德曼的结论是谨慎的。在他看来，智利的转变是一个“令人惊讶的政治奇迹。一个军政府支持改革，使国家的作用迅速减弱，将控制从由上而下该为由下而上。”（第518页）<br />
<br />
当然，智利的例子并非奇迹到绝无仅有的地步。韩国的历程与智利极为类似。不过，弗里德曼依然认为，“智利的情况是一个例外而不是遵从规律的产物。军队实行等级制，其人员注重传统，有的人发号施令，有的人服从命令。它的组织结构是自上而下的。自由市场正好相反。它是自愿的，权力是分散的，它的座右铭是讨价还价而不是服从。它的组织结构是自下而上的。”（第518页）<br />
<br />
也就是说，军政府、或者说威权主义政府的基本结构和内在精神，是与自由市场不相容的。<br />
<br />
因而，对于经济自由的政治后果，人们只能寄希望于奇迹。在弗里德曼看来，至少从理论上，人们不能证明，栖身于政治不自由框架中的经济自由，能够自发地拓展政治自由的空间，相反，自由市场反而可能会被僵化的政治安排所扭曲。在讨论智利的那篇文章中，弗里德曼最后说，“我预言，自由市场经济不可能持久，除非军政府被一个信奉自由政治的民选政府所代替……”。当然，智利人民是幸运的，后来他们得以和平地赶走了皮诺切特，获得了完整的自由。<br />
<br />
哈耶克更为激进<br />
<br />
对于同样的问题，弗里德曼的意识形态同盟哈耶克的看法似乎要更为激进，他认为，在当时的智利，皮诺切特是必要的，尽管属于一种“必要的恶”。<br />
<br />
这一立场更不为学院知识分子所容。即使在哈耶克思想的支持者中，他的立场也是极具争议性的。去年十二月底，在一个专门讨论哈耶克思想的邮件组中，“哈耶克与智利、皮诺切特”的主题，引来了一番非常热烈的讨论。<br />
<br />
哈耶克曾经在1978年8月3日的伦敦《泰晤士报》上发表过一篇文章，直接涉及到皮诺切特，同时也涉及到了个人自由与民主、威权主义的复杂关系问题。哈耶克说，“我从来没有主张过，威权主义政府比民主政府更有可能确保个人自由，我说的恰恰相反。但这并不意味着，在某些历史环境下，在一个威权主义政府下，个人自由能得到比民主政府下更好的保护。自雅典民主制以来，这种事情时有发生……而在现代，也当然有很多例子说明，在威权主义政府下，个人自由比很多民主制度下更为安全。”他举了葡萄牙的萨拉查统治早期，和皮诺切特。哈耶克又进一步解释说：“受到制约的民主制度，很可能是人类已知的最佳政府形态，但这并不意味着我们在任何地方都可以拥有它，甚至也不意味着，它本身就是一种最高价值。”<br />
<br />
很刺耳，但仔细分析，却大体合乎哈耶克的理论逻辑。在哈耶克对于政体的排序中，最优良的当然是自由民主制，即受到制约的民主制，它将价值（即自由）与手段（即受到限制的民主制）最完美地结合起来；其次是能够保护个人自由、包括经济自由、财产权的威权主义制度；与其大体相当的是忽视自由甚至反自由的民主制；当然，最糟糕的是现代极权主义。<br />
<br />
选择“次优”易惹争议<br />
<br />
对于第一种和最后一种，弗里德曼和哈耶克的立场都是非常清楚的，反对他们的人士似乎也没有太大疑问。问题就出在中间两种，两害相权取其轻，但这就需要对两种制度下的个人自由进行计算、对比―――这本身非常荒唐，但却又无法回避。也就是说，在他的心目中，到底何种自由更重要，更根本，何种自由暂时缺乏也可以容忍？<br />
<br />
这样的判断是因人而异的。但总的说来，古典自由主义者可能会选择前者，而倾向于左翼的自由主义者会选择后者。柏克宁可要欧洲的君主制，而主张全力围剿巴黎的革命政府。哈耶克与弗里德曼也认为，在某种独特的威权主义制度下，经济自由、财产权得到较完整的保护，哈耶克还会强调，人民的宗教、教育、传统、习俗等等也基本上是自由的，因而是可以忍受的。<br />
<br />
这不是一个终极性的价值断言，而是一个基于现实的政治抉择。<br />
<br />
人们当然可以继续批评哈耶克和弗里德曼的选择。但任何学者，只要是在次优选项之间进行选择，就不可避免地惹出争议。毕竟，现实远没有理论那样清晰。而将现实想象得如同理论那样清晰，则可能属于知识上的不诚实。</div>

							
							
							
							
