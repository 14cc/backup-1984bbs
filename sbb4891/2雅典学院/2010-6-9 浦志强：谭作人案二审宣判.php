各位师友：<br />
&nbsp; &nbsp; 按成都中院6月6日临时通知的要求，我于6月9日上午10点，到该院第五法庭出席谭作人案二审宣判典礼。谭作人提起的上诉被驳回，四川高院裁定维持了对他的五年有期徒刑和三年剥夺政治权利。差事办砸了，让老谭为件没影儿的事真坐满五年，这证明我和夏霖的水平还亟待提高，我们没能为北京律师形象增光添彩，辜负了大家。<br />
&nbsp; &nbsp; 仪式由一审合议庭原班人马出席，历时约十二分钟。庭前我曾举手要求发言，但刘函看我一眼面无表情，没搭理我，迅速敲槌开庭切入正题。我想请教，既然两级法院都在成都，谭作人也羁押在成都，本案不存在异地因素，何以高院会委托一审宣判。由于本人没见过组成二审合议庭的三位法官，我很好奇他们到底是何方神圣。<br />
&nbsp; &nbsp; 谭作人身体看上去不错，心情很平静。刘函宣读裁定的过程中，我和他眼神偶有交集，彼此点头颌首。街面上安保措施严密，不少各色T恤斜挎背带的豪杰，时常拽对讲机咕哝，估计是徐科长的同事，各角度有人拍摄取证，手里家伙比老艾的机器不次。其实，昨天已有朋友电话打不通，有人还来电致歉，称今天无法亲临。意外的是，广告公司的小客户陈云飞居然到了，他边上几位国保满头是汗，嘴里飞哥飞哥叫个不停，要他另找地方喝茶。我还看到有位女士很客气地劝他，但他跟人家发火：“男女授受不亲，你千万不要碰我。”<br />
&nbsp; &nbsp; 相比之下，今天的气氛与去年8月12日开庭和2月9日一审宣判略显缓和。我的手机关机后可以带进去，谭夫人王庆华也承蒙临时给了张旁听证，进去见了他的夫君。过大门安检我见刑一庭徐庭长，彼此打招呼有交流，我向他们道乏，他们说是为了工作。我说你今天的工作没意义，他说所以才需要律师。我说我当不好牌架子，又不能适时点炮，很惭愧。<br />
&nbsp; &nbsp; 但谭作人的大女儿，因为在大街上拿出包里摄像机，被身边人抓进法院，在里边喝了一个多小时的茶，宣判结束经我和庆华前往交涉，才放了出来。<br />
&nbsp; &nbsp; 德国和美国领事馆的外交官到场，与我有短暂交谈。他们对判决结果表示遗憾，对不能真正公开，感到不解。有本地一位女士劝他走开，因为她穿便衣我有些失礼，请她不要干扰我们的谈话，请教后才知道是本地外办的。还接到北京市某领导的电话和短信，建议我依法申诉和表达意见就可以了，希望尽快离开现场尤其不要接受采访。找到谭作人大女儿后，我们离开了现场。<br />
&nbsp; &nbsp; 近日我还将返回成都，去会见老谭并征求他对的意见，假如执迷不悟继续委托，我们会在完善手续的前提下，为他提起申诉。<br />
&nbsp; &nbsp; 相对于一审而言，我已平静得多，有些悲哀，但不再愤怒。<br />
&nbsp; &nbsp; 多谢各位的关心和帮助。<br />
&nbsp; &nbsp; 浦志强 2010年6月9日 成都</div>

							
							
							
							
