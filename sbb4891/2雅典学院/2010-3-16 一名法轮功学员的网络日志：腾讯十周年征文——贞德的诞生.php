【回忆2003年】 &shy;<br />
<br />
　　妈妈回来了，6。10部门仍然不放过她，继续全方位的进行他们的盖世太保行动，说她并没有放弃信仰，要把她再次送去劳教。他们肆无忌惮的当着所有人的面大叫：要么死，要么放弃信仰！死了尸体也要上缴国家！ &shy;<br />
<br />
妈妈的神经衰弱还没有稳定，身体刚恢复就趁夜出走了，全国流浪。一路躲避特务们的追捕，走的匆忙钱没带太多，在广州垃圾堆里拣东西吃，但是她不能联系我们，一切通讯都被监视着。有人说你父亲多聪明，写了保证，放弃信仰就可以了啊，信仰值几个钱？能好好生活就可以了。 &shy;<br />
<br />
好好生活？想得美，没有人能好好生活，当年的地主，资本家，学者，一批批的全都无条件的屈服了，面临的是要斩草除根。苟且偷生的日子也是不得安宁的。我的父亲每天处于被监视中，监视他的，是被当地安全局收买了的他熟悉了十几年的同事，他每天说话要高度警惕，防止被人套走什么消息，电话干脆不用。甚至过年，他的楼下都有一辆可疑牌照的车。 &shy;<br />
<br />
我们在盼望，出现一位好皇帝，来改变这一切。16大胜利召开了，新皇帝上台了，我们希望他给我们平反，他出国访问，我们打出了“欢迎胡JT，公审江ZM”的横幅，可是各地的监狱中的血腥气息依旧扑面而来。我们在企盼，国际能主持正义，可是联合国说大陆现在很好很强大，要让它开办人X权奥运会了。美国总统说大陆是最好的经济合作伙伴，是的，只剩下经济了。 &shy;<br />
