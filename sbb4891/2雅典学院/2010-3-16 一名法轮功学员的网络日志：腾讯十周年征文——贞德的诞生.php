&shy;<br />
<br />
&shy;<br />
<br />
【回忆2009年】 &shy;<br />
<br />
春天，我和一个熟悉的阿姨碰面，虽然她40岁了，可是她很漂亮，显得那么年轻，我总是习惯叫她大姐，在经历那么多风风雨雨后，她仿佛真的成了我的大姐。多年的感情，抹灭了我们年龄，职业上的细小差别的痕迹，她常常带给我一些我妈妈的消息，鼓励我在个人修为上要像大雄狮子一样奋勇精进，不论遇到什么样的歧视，漫骂，都要放下个人恩怨感情，耐心的讲述事实真相。我常常苦恼于无尽的孤苦中，她对我，就像雪中的炭。然而这次的见面，竟是永诀。<br />
<br />
在这次我们分手后，她的手机被跟踪，特务包围了她家，逮捕了一家人，一个星期后她被打死在北京牛街派出所。验尸报告上写着她畏罪自杀。<br />
<br />
她保护了我，一句话也没有说。她全家被抄空，财产全被警差瓜分了。丈夫至今被关押，生死未卜。<br />
<br />
我得知这个消息时，已经在汕头演出了。面朝着大海，春暖花开，我又一次泪流满面，为了安全，把手机丢进了大海。同事问我怎么了。我说我失恋了，又失去了一个爱我的人。“为什么要说又呢？”说着他们给我照了张相片。<br />
<br />
10年来，噩耗像雪片一样传来。几乎每天都有同门被杀害。我已经习惯在一个人的时候失声痛哭，而在公开场合下绽放职业的笑容。<br />
<br />
 <br />
<br />
<br />
我们一直在等待，等待官府自己幡然悔悟，自动改革变好的漫长苦旅10年后，杀戮依然在暗中进行着。我们给了它们足够长的时间，但是毒药是不可能自己变成甘露的，老师要我们做好人，可好人不是“好欺负的人”，不是逆来顺受，不是面对鬼头刀一声不吭。很多大陆人说，只有两条路，一种是走改革的阳关道，一种是走用力推倒的独木桥。 &shy;<br />
<br />
独木桥谁也不想走。 &shy;<br />
<br />
仿佛只好走阳关道。可是你们凭借什么来使它改革？它会和你们妥协么，它会放弃绝对的权力么？你们没听到“100年不变”么？难道你们还要去做谭嗣同吗？想改革只是你们的一厢情愿，与虎谋皮，身负血债的变态连环杀人狂是没有回头路的，它只能选择毁灭下去。 &shy;<br />
<br />
世界是多元的，智慧是无限的，不是只有这种“要么，要么”的对立选择。我们选择第三种。解体坏人组织，和平过渡到一个没有邪恶组织的中国，没有腐败，没有杀戮的中国。 &shy;<br />
<br />
在目前大陆，人们虽然也对它不满，可是不知道路在何方，在等待，在寻找，在辨别。 &shy;<br />
<br />
而经历过血的洗礼后，我们更加明白自己该走的路---------我们将和所有民间明白真相而觉醒的人一起，和平，理性，去对各界讲述真象，人们在明白真相，悄然退出坏人集团，和平的退出它，不再是它的一员，每退出来一个人，它的邪恶势力就削弱一分，直至彻底消散在风中。甘心做坏人的不多，死心塌地做党员的人，微乎其微了，当它们不能裹挟更多人去助纣为虐时，它的力量就越来越衰微，它就无法再肆虐在华夏大地上，它会无声无息的，自动消失在历史长河中。没有战争，没有骚乱，没有流血，没有冲突，我们会和平过渡到正常的文明社会，更多的许志永会得以平安，更多的邓玉骄会得到昭雪，更多的民间英雄会获得自由。 &shy;<br />
<br />
虽然它貌似很强大，有7500万党员，还有近10亿的后备队-------团员和少先队，可是我们会不断的接触他们，融化他们心中的被谎言所制造的仇恨，和他们讲述这些道理，激发他们的良知，获得他们的理解，一个个退出来。即便它是大山，我们默默无闻的做愚公，总有一天，可以让谎言土崩瓦解，让所有的中国人了解真象，从而做出自己的选择------退出它或者是与它一起走向历史的终结。我们研制开发的对抗金盾工程的利器-------破网软件自游门，在大陆成为最流行的上网工具，无论是否支持我们这种信仰，人们都喜欢用他。在一些城市，甚至有网警在公开出售这种软件，附送一套书《9评XX党》。 &shy;<br />
