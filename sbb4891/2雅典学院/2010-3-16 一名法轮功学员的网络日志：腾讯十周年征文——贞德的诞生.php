腾讯十周年征文-----贞德的诞生（上，从97年到2006年） <br />
<br />
【回忆1997年】 &shy;<br />
<br />
　　我进入了军艺。在这里遇到了我生命中的点点繁星，一群陪伴了我10多年的朋友们，什么是朋友？就是上天给你自由选择的没有血缘关系的亲人。他们陪伴我走到今天，见证着我生命中的喜怒哀乐，他们给了我无比的勇气来面对恐怖的压力，他们是我的家人，是我的坚强后盾。&shy;<br />
<br />
 &shy;<img src="http://www.fish3000.com/attachments/month_1003/20100316_ea1b06a28d4f3ce9aadco3xeFg1cMXci.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
&shy;<br />
<br />
这是我生命中的一个起点。我离开家乡，开始了独自的生活，独自认识这个世界，用我自己的最单纯的良知来建立世界观，并以此衡量一切，我永远学不会猜疑和背叛。军人这个身份，将给我的灵魂打上深深的烙印。我爱部队，我愿意用一切去维护所有真实的，善良的生命，一花一叶，一人一物。<br />
<br />
　　 &shy;<br />
<br />
【回忆1998年】当时北京的气功热潮感染着每个人，连出租车司机也在谈论92年在国贸大厦的东方健康博览会上的那个功法，如何神奇，京城人人都知道有个李先生，他免费给广大见义勇为的负伤英雄治疗，并在工安礼堂给许多闻名而来的老干部讲课，顿时在军区中也是名声大震。他的书最便宜，假如没钱，还可以白送。于是我也买了一本，一下被其中的理论所震撼了，折服了。我在和家里联系后得知，原来爸爸妈妈也在早晨去公园锻炼时开始练习这种既祛病健身又指导人提升内心境界的功法了，原来中国有近一亿人在学呀。练气功，不止是为了健身，我相信书中的教人向善，做一个最纯洁的好人，不受世俗标准约束的更高尚的人，正是我一直向往，追求的人生目标。我找到了人生的真谛，又有活泼可亲的朋友们，这是我人生中最最幸福的一段时光。<br />
<br />
 <br />
<br />
&shy;<br />
<br />
<br />
&shy;<br />
<br />
<br />
 <br />
<br />
【回忆1999年】 &shy;<br />
<br />
　　做好人是快乐的。因为坦诚，因为无私，因为明白了美好的意义，人生有了目标。说真话，办真事，做真人。<br />
<img src="http://www.fish3000.com/attachments/month_1003/20100316_942c619e850e6dfb369cATGuxLGm2FbL.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
可是美好的生活总是那么短暂。<br />
<br />
 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;而到了99年的夏天，我和很多人一样，陷入灭顶之灾。因为我和亿万练气功的人们一样，一夜之间，都被官府打成了X教成员。我们从模范公民，好人成了非法的人。好和邪，这个定义标准竟然可以由官府来制定，我们不能理解，为什么我们好好的生活，好好的 做人，好好的锻炼身体，会成为人皆可杀之的X教？无辜被凌辱，被殴打，被关押，我们成了“非人”的动物，我们走投无路，只好去 伟大的首都，找最高领导人申诉。当然我们都清楚，这意味着什么，10年前的六月的恐怖阴影降临到了我的头上。可是我们没有别的出路。 我们心里都相当清楚自己的下场---------如果我们不为自己辩护，说清楚这种功法的健身效果与清白，那么迟早我们会像当年的地主，资本家，知识分子一样被打成党的敌人，人民工敌，我们迟早会被一批批的杀掉，以“合法的罪名”。<br />
<br />
一想到这里，我就颤抖。连小小的我也给各级官府写了很多信，大约数量超过这辈子其他时期的总和。据说中nan海和信仿办每天都要烧掉半吨这类发自全国各地的上仿信，民众的心声。我清楚的记得，这一年北京的夏天出奇的热，有43度。7月20-22日去北京中难海上仿的信徒，就有10万，中难海害怕了，非但不给予解决问，反而污蔑这些人是“攻击政府”，手无寸铁的百姓何谈攻击？而上仿鸣冤的人都是弱势群体，他们求助青天大人，怎么敢故意冒犯？如果没有天大的冤情，谁愿意一次次跋山涉水突破围堵冒险赴京，而且是顶着死亡的威胁，难道国家给报销住宿和差旅吗？而陆陆续续的不知有多少人奔赴北京，据报表统计馒头厂大发财，每天多卖200万人的份。我给朋友们写了遗书，说假如9月还没见到我，那我的血一定在广场的地板砖缝隙里。<br />
<br />
&shy;<br />
<br />
【回忆2000年】 &shy;<br />
<br />
　　全世界的焦点，集中在了世纪末的中国。这个信仰善良的群体，就因为人数多过党X员的数字，江XX出于嫉妒，利用手中绝对的权力，不顾举国反对，也无视宪X法保护信X仰自XX由的权X利，在全世界的目光中，抛弃了民心，把这些人统统打成了党的敌人，把国家和民族也拖下了水。<br />
<br />
老一辈人最熟悉，也最恐惧的“运X动”，又开始了，这次的口号，是打击X教，（至于前半句的提倡科学，那是修饰词，主语是打击）范围是所有公民。既然有一亿人，那么打击了他们，他们的亲人朋友同学同事至少有3亿人被牵连涉及。。。。都直接或者间接的遭到了迫害，所以我说，这是针对全民的，没有人可以回避，没有人逃避得了“运动”。迫害开始，舆论先行，CCAV先捏造自杀，杀人，练气功会得精神病的新闻，把这些好人妖魔化，一些平时不怎么看CCAV的人，也禁不住漫天遍野的假消息，信以为真，并且还以为这个结论是他自己个人的判断。而少数一部分了解事实的人，则对此事讳莫如深，不敢谈及。于是一个谎言在13亿人中流传开了，以讹传讹，绘声绘色，像感冒病毒，交叉感染，生生不息。谎言过后，暴力登场了。 &shy;<br />
<br />
&shy;<br />
<br />
2000年，人们在欢庆走过的重重危机的世纪。谁也没注意到那个时间段里，中国大陆正在发生着的浩劫。无数信徒们背负着巨大的压力，坚持着自己的信仰，遭受着白眼，侮辱，殴打，告密，失去工作，学籍，无辜遭到逮捕，甚至是流血，牺牲。度日如年。可是有谁关注过我们遭到的不公正对待？ &shy;<br />
<br />
那时，我还不知道红色的恐怖将会蔓延11年之久，超越一个文革。 &shy;<br />
<br />
&shy;<br />
<br />
【回忆2001年】 &shy;<br />
<br />
　　一天早晨，安全局的特务冲到我家里，抓走了我的妈妈，说她顽固信仰，破坏了社会秩序。原来思想也是罪名。可是一个人只在家里练练气功，相信这种气功的功效，功理，又怎么破坏了秩序呢？？ &shy;<br />
<br />
没有人回答，但是却有人执行。不准请律师，没有审理过程。直接劳教3年。 &shy;<br />
<br />
 <br />
<br />
我们还要参加“顽固分子家属学习班”，“学费”是5000。爸爸在压力面前，在保证书上签字：我再也不信任何东西了，我会重新做党的顺民。口说无凭，要当着执法人员的面破戒，重新吸烟，喝酒，打架，骂人，贪污，包养小秘，才确信你真的放弃了忍，真，善的信仰，才算真的回到党的怀抱。然后我父亲被迫和妈妈划清界限，离婚，这场离婚，是当地官府“瓦解X教”的一项政绩，上了报纸，办理这件缺德事的官员们得到了升赏，我没有了家。 &shy;<br />
<br />
&shy;<br />
<br />
后来没多久，就听说在狱中的妈妈被拷打得不成人样，生死难料。我和父亲一起流了很多泪，就像张志新的丈夫和女儿，低声哭泣，却不敢让邻居听到。邻里羡慕的美好家庭，变成了人人避之不及的灾星。 &shy;<br />
