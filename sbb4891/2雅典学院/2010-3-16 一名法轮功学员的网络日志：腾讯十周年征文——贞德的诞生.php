 <br />
<br />
感谢我主，让她还能回到我们身边。又有多少像她一样的人，再也没见到亲人？ &shy;<br />
<br />
长春的气功学员在当地晚间电视新闻里插放了一段关于《焦点谎谈》炮制的那段自棼的慢镜头，并且指出了其中造假的破绽多处。长春数百万人在电视上看到了真象。震惊之余，人们开始质疑当局的做法，据说总书记“雷霆震怒”，一连下了几道秘密文件，工安部长罗干亲自跑到长春去抓人，5000人被投进黑墙，连夜审讯，打死十几人，抓捕过程中，不惜烧山包围，活活烧死一人。 &shy;<br />
<br />
海内外的1亿信众觉得很难忍受了，老师说，我不是带你们闹革L命来了，我们不为权力，我们不沾染政治，你们要知道自己走的是一条什么路，人们被媒体的谎言所毒害，说我们是邪X教，对我们做了很多不好的事，可是我们就是对他们讲述真象，你害我，我不恨你，我告诉你这一切。人会分辩真伪，会从我们的身上看到打不还手，骂不还口，人们会理解，会感动，会有所选择。基督徒们也被罗马政府定为邪X教，他们用了300年的时间去对民众讲述他们的遭遇，才换来了他们的被承认。假如这样的日子还有10年，你们还干不干？ &shy;<br />
