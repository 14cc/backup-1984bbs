【回忆2002年】 &shy;<br />
<br />
　　为了推动全民斗争，CCTV捏造了所谓天按门自0X焚案，焦点谎谈把精心炮制的带子送到联合国教科文组织，马上就被识破了-------是国家导演的伪火闹剧，目的是为杀戮有信仰的民众制造舆论借口。(详情请参照另一篇日志《穿帮电影点评》）联合国对大陆提出严肃警告，并意识到70年前犹太人遭到的种族灭绝罪将在大陆的这些有信仰的民众身上重演。但是在封锁消息的大陆，反映激烈，这么一出破绽百出的闹剧，就让很多本来支持信仰自由的人改变了态度------法律没有了，仇恨蔓延到所有人的头脑里。原本以法律为依据支持信仰自由的民众们，都倒在了强权的一面。他们喊着：对轮子，就不能讲法律！ &shy;<br />
<br />
我心里满怀忧伤。这一年里发疯的变胖。胖到自己都吃惊。我在军艺毕业了，加入了文工团。成了一个专职演员。 &shy;<br />
<br />
&shy;<br />
<br />
 &shy;<br />
<br />
&shy;<br />
<br />
得知妈妈还活着。但是被连续的刑讯折磨得已经精神恍惚，疯疯癫癫，说不出逻辑完整的三句话。她浑身青紫。长期的吊铐，磨掉了她的手腕上的肌肉，露出骨头，腕骨也断了。可是每天被迫长时间从事奴X工劳动，让她包筷子，每天要包出几麻袋的筷子，包到手指出血，血淋淋的浸染了那些包装纸。狱卒大怒，因为那样会使筷子卖不出去，影响他们的奖金。在冬季里罚她光着身子在操场上站着受冻，直到昏死，又用凉井水泼醒。劳教所怕她死在里面，于是保外就医，送她回来了。同时又敲诈了我们一大笔钱。 &shy;（据统计，中国大陆每年生产约800亿双一次性木筷，）<br />
