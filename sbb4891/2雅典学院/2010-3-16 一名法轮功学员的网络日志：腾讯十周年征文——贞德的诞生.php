【回忆2004年】 &shy;<br />
<br />
我在北京认识很多外国友人，他们在北京定居多年，热爱中国，我和其中一个开小酒吧的白人说到我的遭遇。他说，我不太懂你们中国的法律。不过我很为你担心。电视里公开宣扬仇恨，在其他任何一个国家都是不可想象的，电视台居然丝毫不考虑法律问题，最奇怪的是居然没有人管。我告诉在我这里喝酒的客人，你们的电视像疯子，他们说“我们的电视平时都说假话，可这次对待法X功问题我们相信那是真话。”杀戮暗示已经很明显，中国人居然还那么镇定，那种气氛，没经历过的人真的很难知道。当时我看完电视，以为又要实行98年印尼那样的事了，吓得我赶紧关了酒吧，买机票打算逃离。<br />
<br />
我告诉他们，在你们的国度，政治和信仰是井水不犯河水，平行的，每个人的信仰像吃饭饮水晒太阳一样平常，而在我的祖国，为争取信仰的自由就要用生命来做代价，就像当年你们的基督徒一样。这和政治无关，和20年前的大学生不同，他们会为主义激动，也会为主义的失败而无奈的放弃。但是信仰是不会改变的，即便只剩下最后一个人。<br />
<br />
他低头想了一会，说：曲，你很像贞德。我为你祈祷，希望你不要遭遇不幸。你知道----虽然现在是人们穿西装的时代了，可是中国人会像中世纪烧死贞德那样对待你们的，一点也不会文明。中国人有多少比例支持你，我不知道，但是我觉得全世界大部分人都会支持你们，因为你们真的很好，很善，很COOL。<br />
<br />
 <br />
<br />
我告诉他，为了消灭这一群坚持和平理性，坚持信仰不屈服的人，官府可耻的多次派间谍去海外暗杀我的老师和同门，又动用了1/4的国库，在全国新建立了大大小小无数监狱，把各行各业中的先进个人，好人，精英，劳动模范抓进去刑讯逼供，逼迫他们放弃信仰，最小的这种“因思想犯罪的囚犯”居然只有7岁，最大的90岁，只要他们被迫签字说不练了，就被押解着去CCTV新闻里做无良伪证，假如拒绝屈服，那么打死不计，方法随意，死后不查身源，直接火化。不完全调查约关押了近百万之众。罪名五花八门，很多是像我母亲那样的“用思想破坏社会秩序”，刑期3-12年不等，或者根本无须罪名，无须审判，单单一个无限期的劳教就可以让你永远见不到太阳。 &shy;<br />
<br />
　 夏天，我与一个和妈妈一起患难的狱友（共同信仰者）见了面，一起吃了饭，她说很想3岁的女儿，可是不能回家去和亲人团聚，还有更重要的事要做，这个城市需要我。这个城市的人不了解我们被迫X害的真象，我要告诉他们，信仰是无罪的。多一个人，就多了一条让人们了解这一切的渠道。 &shy;<br />
