																											<div id="postmessage_518218" class="t_msgfont">腾讯十周年征文-------自传（下）从2007年到现在 <br />
&shy;<br />
<br />
【回忆2007年】 &shy;<br />
<br />
　　国家在这种无形的内战中，花费的金钱已经远远超过了两次海湾战争的总军费。光天按门一处，每天动用的人力薪水就是100万，财政渐渐支持不下去了。邪恶的败像已显。我们已经在国际法庭起诉，把江XX，罗X，周永糠，苏荣等一干凶手等告倒了，他们一旦出境，就像纳粹战犯一样面临国际法庭的审判。 &shy;<br />
<br />
&shy;<br />
<br />
形式渐渐好转，混迹在我们中的特务们编造的假消息已经不能迷惑人了。在血和泪中我们讲述这一切，即便饱受杀戮10年，在全世界却没有一起的恶性报复事件，老师的高大形象赢得了全世界人们的赞叹，老师连续多年获得诺贝尔和平奖提名。----------某党在全球抹黑我们的计划彻底破产了，全球都看清了它们的卑鄙伎俩和丧失人性的暴行。越来越多的各种肤色的民众走上街头，为我们的不公发出呼吁。 &shy;<br />
<br />
&shy;<br />
<br />
我们有了三大媒体---明汇网，大记元报纸，信唐人电视台。我们只有用自己的声音说话，这声音，不在冠冕堂皇的政坛大报，不在一掷万金的电视台，不在声嘶力竭的商业网站，他如涓涓细流，在民间，在最底层，在每个有人烟的地方。 &shy;<br />
<br />
&shy;<br />
<br />
大陆有几十个集中营，在卖活人的器官，现卖现割-----既然总书记说了，不查身源，直接火化，打死算自杀，那么信徒的尸体也是无主的，角膜，肾脏，都可以拿去卖。几千万信徒，真的是一个很庞大的财源。登记在册2001-2006间的器官出口量是2001年前的几百倍。 &shy;（详情请看另一篇日志《“被统计被代表”的背后，还有“被捐献”》<br />
<br />
可是统计在册的枪毙犯人数目却没有增加。很明显是黑色交易。 &shy;<br />
<br />
至于没有登记的暗中交易呢？我只知道，外国每年都从大陆买走成成千上万的新鲜尸体做标本，甚至开了一个很有名的巡回人体展览。也许那里就有我的朋友X，Y，Z。。。。。胡皇帝啊，您是否是商纣的转世？ &shy; &shy;<br />
<br />
胡皇帝和江皇帝没有区别，江皇帝愚蠢，胡皇帝作秀，用一种诚恳敦厚的表情收买着民意，继续维持着江时代的血腥摊子，他还在网络世界发明了“河蟹”，既然无法在民间继续制造仇恨，那么就在沉默中虐杀，掩盖一切试图揭露这些人间地狱的信息，在“盛世”下继续杀戮，工安部在他的授意下，更像明朝的东厂了，网上的一句言论可以使人丧命。<br />
<br />
 <br />
<br />
【回忆2008年】 &shy;<br />
<br />
<img src="http://www.fish3000.com/attachments/month_1003/20100316_101473e1df5adf88fc0b8xG7ZI0n2VrF.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　由我的老师李先生亲自导演的神X韵艺术团获得巨大成功，在全球巡回演出。我们文工团奉命出国和神X韵争夺观众，理所当然的获得了失败。神X韵是最棒的，无可媲美的。我们成了公费出国旅游，大家笑嘻嘻。 &shy;<br />
<br />
 &shy;<br />
<img src="http://www.fish3000.com/attachments/month_1003/20100316_bde3d2b1aa28e6d0ce09c3cM6IeCmshj.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
&shy;<br />
<br />
&shy;<br />
<br />
&shy;<br />
<br />
领导也没什么可说，因为赵本山都出动了，还是惨淡收场。神X韵更加势不可挡，各国政要或者携家眷出席首映，或者发去贺电称赞神X韵代表了真正的中华舞蹈艺术。神韵成为了世界超一流的艺术团体。票价达到500美元，纽约观众说，神韵的伟大演出，值这个价钱。全球80多个国家纷纷发出邀请函，同时私下表示支持李先生的义举，非暴力不合作行动赢得了全世界的高度关注。<br />
<br />
 <br />
<br />
&shy;<br />
<br />
<br />
<br />
<br />
<img src="http://www.fish3000.com/attachments/month_1003/20100316_bde3d2b1aa28e6d0ce09c3cM6IeCmshj.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
&shy;神韵纽约首场 名流盛赞顶级 华人泪流不止 <br />
<br />
<br />
在地球上，仅有一个地区，仍然对这种功法进行着血腥的迫害。大陆的人出国旅游，会惊讶于海外的每一个角落都在控诉中国对信仰的罪行，起先大陆人会万分震惊，然后是愤怒，大喊IT IS NOT TRUTH！（那不是真的）可是假如他们还有思考的能力，他们就会想到，新加坡，日本，韩国，台湾，加拿大，澳大利亚。。。。全世界难道串通好了的，10年来坚持不懈的，异口同声一起合伙捏造这么大的一个“假新闻”吗？可是甚至连一江之隔的同样是中国领土的香港，也是漫天的为信仰呼吁的声音，难道香港也属于敌对势力？<br />
<br />
发生在大陆的事，大陆的人们往往在全球范围内最后一个知道。 为什么呢，他们看不到世界，他们只能看到官方允许他们看的谎言，久了，就把谎言当成真理。就像饥民没有粮食吃，只能吃官府发的观音土。官府说这是仙土，吃一顿顶五餐，因为吃了会涨肚，吃多了会死掉，可是人没有选择硬着头皮只能吃，等到有粮食吃的时候，也习惯性的排斥，说我才懒得吃普通粮食，我要吃仙土。人人都在喊着要听真实的新闻，可是当我们讲述事实的时候他们暴跳如雷，拒绝听，拒绝相信。一个平和讲理的人，当听到法X功三个字的时候，面目表情突然会变的狰狞，扭曲--------这就是谎言的作用。谎言可以杀人，可以拖人下地狱。<br />
<br />
 <img src="http://www.fish3000.com/attachments/month_1003/20100316_6e223c3bcba3b8158196AvufZmAXJzmN.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
姚姐说，我觉得你们这种信仰的人都很不正常------当然，除了你之外。其实她这么说，并非认识更多的有信仰的人，而是经历了谎言的洗礼，大陆人“思想中的观音土”发挥作用了，一致认为，坚持信仰的人都是精神病，都是恐怖分子。他们很难把自己熟悉的亲朋与官方宣传的丧失人性的恐怖分子联系起来，才导致这种恐慌和排斥。从10到60岁的人，平时可以坐在一起兴致勃勃的大骂贪官亡国害民，可是一谈到法X功被迫害，人人脸色惨白，说我不参与这事，别和我说，我不想听。<br />
<br />
这是受谎言毒害极深的症状。如果是一个正常人，假如有他不知道的事，好奇心促使他会一直寻找真相，多渠道的消息加以多方对比，得出他的判断。有人说，这是民众生存状态决定的，底层百姓忙着讨生活，不想关心闲事。只有高层知识分子才会考虑社会矛盾问题。<br />
<br />
然而根据我个人的经验来看，完全不是这么回事。在《皇帝的新衣》里，真正能发现真理的，恰恰是单纯的底层民众。底层百姓也是人，也具备人的良知和好奇心，他们的头脑并不是单工齿轮发条，在养家糊口的同时他们也可以有着最朴素的价值观，不需要什么凯恩斯也不需要什么零几县长，足够分辨好与坏，正与邪，所以往往我在与低层生活者交往中，讲述这些事一般容易被他们理解和接受，他们会为之叹息，并做为活传媒主动去消除其他人心里的疑惑。伟大的良心，依旧在不起眼的土壤中跳动。<br />
<br />
而知识分子往往受自己学到的那点党文化教育的限制，由无神论为基点从内心鄙视一切信仰，认为自己看到的消息，才是唯一正确的，别人的信息都是小道消息，不值得相信。他们本该承担起驱散谎言迷雾的社会义务，可是他们恰恰却助纣为虐，帮助恶党制造谎言，传播毒素，歪曲事实甚至主动捏造假新闻。原《人民日报》论坛副主任邱明伟说，当时我们每个新闻工作者都有“制造法X功假新闻”的定量任务，我们自己编造，虽然粗劣，可是数字太庞大了，无处不在，最后连我们同行看了都信以为真。其中有这样一件事，某乡下妇女修炼法X功，某天她抱着孩子过桥，不小心掉到河里，于是被我们在新闻里写成“抱着孩子跳河，追求圆满”。我们下属的一个记者看了这个新闻，当天气的没吃下饭，蹦着高的骂李XX害人--------我们当时苦笑，无奈，私下告诉他那是我们编的。连一个明知道新闻是虚假的记者最后都相信了邪教之说，可见铺天盖地的宣传的作用。后来那妇女发现了这件事，在法X功的网上刊登了她的声明，澄清事实，不知道有多少人能联系起来当年的这么一件“新闻”。<br />
<br />
知识分子，他们讨厌“迷信”，他们不承认无论是迷信还是宗教其实都是受法律保护的。在他们看来，“迷信”就该被扫灭，法律应该袖手旁观。他们忙着抒发个人观点国际形式，纵横古今，对三年自然灾害饿死的3000万中国人悲天悯人溢于言表，却无视当今1亿同胞的凄惨遭遇，叶公好龙。他们大谈民主兴国，却仍旧歧视信仰，虚无飘渺的主义，实实在在的歧视。宣扬民煮的人本身不具备民煮精神，也不修身重德，让我很失望，觉得指望他们来主持正义更是遥遥无期。他们大无畏，敢于谈论一切危险话题，但是却不敢讨论信仰，或者不屑谈论，殊不知，一切民主运动，都是由一种曾被迫害的信仰发起的-------基督徒们为使一切民族免遭耶稣那样的迫害，被剥夺信仰权利等一切人生而具来的权利，才产生的民主制度。他们回避法X功问题，可是目前全中国民生民权最焦点的问题正是这1亿人遭到的非法对待的问题，绕过这个根本问题，什么都谈不上。很多人他们一生都受到消息封锁，都不曾看过一本关于法X功的著作，却一本正经的仿佛多么了解这种气功似的大说李先生的坏话，连挤眉弄眼的戏谑神情都和那些CCTV里的御用专家一模一样。对于CCTV，他们是又爱又恨，嘴上痛骂，潜意识里的却一直在模仿。他们说，我们最喜欢人家擅长的专业领域得到一些有价值的见解。能够从别人那里得到一些独特的视角，是一件非常令人开心的事情，相当于捡到了别人钱包里掉出的钱，遗憾的是，当我准备把自己钱包里的钱分给他们时，他们露出厌恶的神色-----甚至比看到一个狂热的毛左还生气。“选择性的民主，嘴上的民主”。<br />
<br />
我真的很想，很想告诉他们事实的真相，以及这个功法的科学性，可惜，我力不能及，我学问浅薄，始终无法使他们垂下那高傲的头，来听一听我说的。我只是个无名小卒，不起眼的舞女。<br />
<br />
 <br />
<br />
有时，我只能对石头去诉说。我不指望他们能点头，至少石头不会拒绝听。<br />
<br />
