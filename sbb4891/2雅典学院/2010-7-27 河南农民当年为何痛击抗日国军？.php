													<h2>河南农民当年为何痛击抗日国军？</h2>
						
						
																											<div id="postmessage_760899" class="t_msgfont">1943年2月，重庆《大公报》上刊载的一篇有关河南灾情的报道，给时任美国《时代》周刊驻华记者的白修德留下了“刻骨铭心的印象”。对此，晚年的白修德回忆说：“1942年是大旱之年，我们在重庆得知，河南的农民正在濒临死亡”。我们今天的话题就从这里说起。1942年冬天，抗日战争进入相持阶段。24岁的天津小伙子张高峰刚从武汉大学政治系毕业，就被《大公报》派往河南担任战地记者。从陕西进入河南时，他被眼前的景象惊呆了：“陇海路上河南灾民成千成万地逃往陕西，沿途遗弃子女者日有所闻；与此同时，物价已经长到不可理喻的程度，许多人被迫卖掉自己的年轻妻子和女儿去做娼妓，而卖掉一口人，还换不回4斗粮食……”。<br />
<br />
中选网转载《国家历史》杂志的这篇文章写道，于是，张高峰奋笔疾书，把此行的所见所闻写成一篇6000字的报道，发表在1943年2月1号的《大公报》上。文章感慨道：“大旱之年的河南，吃树皮的农民，直到今天还要忙着交纳军粮”！不料，这篇报道惹得蒋介石勃然大怒。2月2号晚，他不仅下令《大公报》停刊3天，到3月初，尚在河南的张高峰也被国民党豫西警备司令部逮捕，并遭到刑讯。《大公报》被停刊，激怒了一向为蒋介石说好话的美国记者白修德。白决定和他的朋友、一位《泰晤士报》记者一起奔赴河南，看看那里到底发生了什么。和张高峰一样，这两个外国人也被河南人间地狱般的场面惊呆了：无穷无尽的难民队伍，随时因寒冷、饥饿而倒下的灾民。而最触目惊心的是，父母亲将自己的孩子煮了吃；还有的家庭，把所有的东西卖完换得最后一顿饱饭吃，然后全家自杀……。1943年3月22号，白修德发自河南灾区的这篇报道《等待收成》刊发在美国《时代》周刊上，文章在美国引起了轰动，也带给蒋介石前所未有的舆论压力。尽管后来这位总司令曾当面对白修德表示要整顿此事的决心，但随后发生的事情却让这位美国记者大失所望。的确，“有人为此受到处分弹劾，甚至掉脑袋了”，比如说，洛阳电报局那个将白修德的上述文章发往美国的发报员。<br />
<br />
文章又说，有资料显示，抗战爆发以后，国民党的几十万军队驻扎在河南，军粮、草料、兵源全部都是“就地取材”，从1937年到1942年，河南出兵出粮的数量均列全国之首。但即便在风调雨顺的年头，河南农民在交完赋税之后也只能靠着杂粮和野菜勉强度日，更何况1942年全省遭灾，麦收只有一、二成，秋粮完全绝收！事实上，截至此时，河南至少已经饿死了300万人，但这个数字，却被当时的河南官方统计为：1602人。就这样，在天灾人祸的双重夹击之下，大批河南农民被迫揭竿而起的“历史性一幕”终于发生了。<br />
<br />
1944年，日本在太平洋战场受到重创之后，在中国发动了空前规模的“一号作战”，试图打通直达南方的大走廊。在历时38天的这场战役中，日军以5万余人的兵力打垮了40万人的国军。就在汤恩伯部向西撤退时，这 “历史性的一幕”出现了：大批豫西山地的农民举着猎枪、菜刀和铁耙子，到处截击这些国民党的散兵游勇；后来甚至整连整连地解除他们的武装，缴获他们的枪支、弹药、高射炮和无线电台，甚至枪杀、活埋部队官兵；5万多国军士兵，就这样束手就擒了。<br />
<br />
对此，号称“中原王”的汤恩伯恼羞成怒，把这次中原会战失败的罪责全都推到了河南老百姓身上，破口大骂河南人都是卖国贼！然而，在日军攻克的汤恩伯部仓库中，仅面粉库存就有足足100万袋，足够20万军队一年之用。白修德当时就曾提出质疑，为什么不分出一些用来赈灾呢？有官员告诉他：“如果人民死了，土地还会是中国的；但如果士兵饿死了，日本人就会占领这些土地”。这恐怕也是蒋介石当时的心声，但他们万万没有想到的是，日本人竟然在灾区拿出军粮搞起了赈灾，这也直接导致了河南民众对国民政府的唾弃。<br />
<br />
<br />
&nbsp;&nbsp;综上所述，白修德分析认为：“他们是创造了这个世界上最伟大文化之一的民族的后代。这种文化把社会秩序看得高于一切，如果他们不能从自己这里获得秩序的话，就会接受不论什么人提供的秩序”。白修德写道：“如果我是一个河南农民，我也会被迫像他们当时所做的那样，帮助日本人对付他们自己的中国军队；我也会像他们在1948年所做的那样，站在不断获胜的共产党一边”。白修德是亲国民党的美国记者。他能说出这番话，说明当初国民党政府确实无可救药了。另外，这篇文章也提示我们，要为底层人民提供一种秩序。<br />
