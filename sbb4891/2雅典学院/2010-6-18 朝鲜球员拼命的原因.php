																											<div id="postmessage_689243" class="t_msgfont">对比伊拉克时代萨达姆足球队就知道朝鲜球员拼命的原因了！<br />
<br />
<br />
在萨达姆政权被推翻之后，伊拉克国家足球队队员揭发了萨达姆的长子乌代残酷迫害他们的情况。阿根廷《号角报》日前对此进行了报道： 　 <br />
<br />
　　2002年在约旦进行的一场世界杯预选赛中，伊拉克国家队与阿联酋国家队在90分钟内战成平局。规则规定应互罚点球决出胜负。伊拉克队中场队员扎伊尔口中念着祷告词，紧张地走向对方的大禁区。每个足球运动员都知道罚失点球的痛苦和遗憾，但对于伊拉克国家队的队员来说，罚失点球意味着更严重的后果：忍受公众潮水般的羞辱，然后是长时间的牢狱生活和皮肉之苦。因此，只有3个伊拉克队员有勇气去罚点球，扎伊尔就是其中之一。<br />
<br />
　　扎伊尔说：“当时，大部分队员甚至不愿碰一下皮球，但如果没有人挑起罚点球的担子，全队都会受到处罚。”结果，扎伊尔不幸射失了点球。两天后，国家队返回了巴格达。扎伊尔被召到了当地奥委会的办公室。伊拉克奥委会的主席就是萨达姆总统的长子、伊拉克体育事业的最高领导人乌代。刚到那里，扎伊尔就被人蒙上了双眼，然后关进了一间单人牢房，这一关就是3个星期。然而这只是个开始。现在，萨达姆政权被英美联军推翻之后，乌代就消失了。伊拉克国家队的情况才得以暴光。<br />
<br />
　　根据国家队队员们介绍，他们平时几乎每天都要接受乌代的训话。这位伊拉克足协主席甚至威胁要把他们的腿砍下来喂狗。没有参加训练的人，即使是因为照顾生病的孩子，或参加直系亲属的葬礼，也要被关进监狱。输掉或打平一场比赛，就会遭到电棍的敲打和往身上泼脏水。国家队的队员们终日生活在乌代的监视之下。球员在比赛中出现各种失误，都会根据其严重性受到惩罚。吃到一张红牌是特别不幸的事情。<br />
<br />
　　三年前，前伊拉克国家队队长拉蒂夫被指控在巴格达进行的一场比赛中击打了一名裁判。比赛结束后，拉蒂夫被押到巴格达郊区的拉德瓦尼亚监狱，囚禁在一间仅有两平方米的牢房中，牢房的墙上只有一扇小窗户。他被剃光了头发和眉毛，并被剥去了上半身的衣服。然后，他们命令拉蒂夫做两个小时的俯卧撑。而当他刚刚显出疲劳，三个狱警又开始对他进行电击。最轻的刑罚是在大冬天把他带到院子里，丢进冰冷的臭水坑。<br />
<br />
　　拉蒂夫在拉德瓦尼亚监狱被关了两个星期。被释放后，因为背部剧烈的疼痛，他差不多一个月无法入睡。他说：“伊拉克就是一座大监狱，但除了为国家队踢球我别无选择。我生活在威胁下，如果我不随队训练，他们说会把我当作政府的敌人，甚至要我的命。”对足球运动员用刑在伊拉克体育界是个公开的“秘密”，但国际社会已了解到一些情况。伊拉克国家队队员说，为了欺骗国际足联派往伊拉克的视察人员，乌代亲自负责将那些留有伤疤的球员藏了起来。<br />
<br />
　　人们似乎很难理解从未当过运动员的乌代为什么对足球运动如此着迷。据伊拉克国家队队员说，实际上他对足球一无所知。然而，乌代的步步高升却与足球有关：上世纪80年代中期，他试图利用足球赢得支持率。为此，他自己宣布担任伊拉克奥委会主席和足协主席。而在1996年遇刺受伤之后，乌代在仇恨的驱使下设计了一套对比赛表现不佳的球员用刑的方案。方案中详细规定了刑罚的种类和强度。<br />
<br />
　　像加法尔一样的明星球员遭受的折磨更多。后来，加法尔离开伊拉克，去卡塔尔踢球。但条件是将收入的40%上交伊拉克足协。乌代甚至不允许队员们退役。</div>

							
							
							
							
