													<h2>西方列强为什么老是打我们？</h2>
						
						
																											<div id="postmessage_103576" class="t_msgfont">长期以来，党和毛主席给大家灌输了一个错误的观念，那就是1840年以来，西方列强不停地打中国，是因为中国落后愚昧和弱小。没有比这种谬论更远离真相了，世界上落后愚昧的国家很多，弱小的国家也不少，大都没有遭到侵略征服，国民安居乐业。中国不断挨打不是因为落后，而是因为不断捣乱。 <br />
<br />
落后不挨打，捣乱挨打，这个才是普世真理。 <br />
<br />
大家不服气，我们一点一滴慢慢谈，先说鸦片战争。 <br />
<br />
共产党一直欺骗大家说，英国向中国倾销鸦片毒害中国人民，林则徐禁烟，英国人就发动鸦片战争，挺冤的样子。林则徐1839年3月到广州开始查烟，英国国会1840年4月通过战争决议，6月军舰才开过来，这一年多里，则徐老兄干了点啥，年轻人就不知道了。 <br />
<br />
禁烟我没意见，鸦片猖獗的根源是国民喜爱，禁烟要从根本下手，比如收缴民间烟枪，组织大家学习提高思想觉悟，慢慢把抽鸦片的恶习改了，鸦片不禁自绝。可林则徐唯恐天下不乱，搞起林彪围长春的那一套，居然去把英国的商行包围以来，停水停粮，要人家把鸦片交出来。试想如果今天中国政府突然要禁手机，连夜派兵把人家摩托罗拉，诺基亚在华公司包围起来，停水停电停粮停网络活活困死人家，别国的政府会如何反应？ <br />
<br />
英国朋友义律不想与中国起战事，主动忍让息事宁人，结果则徐老兄得寸进尺胆大妄为，进而派舰船把英国在海面保护商船的军舰也给包围起来，不让它们靠岸补充给养，想把别人饿死渴死，英舰官兵自然不能等死，要上岸寻找给养，遇到清军拦截一急就开炮，后面的就是历史了，这事左看右看，不纯粹是老祖宗们惹事生非没事讨揍吗？ <br />
<br />
鸦片战争后搞了个南京条约，虽说难堪，却也没有亡国，接下来本应该老老实实过日子，可是中国人喜欢捣乱的恶习难改，不到十二年，又出了个动乱分子，叫叶名琛，唯恐中国不亡。1856年，清兵登上一首挂英国旗的叫亚罗号的船只，以涉嫌走私的藉口搜查，如果确有走私根据，扣留船员还说得过去，可这帮捣乱分子楞把英国国旗撕了以示侮辱。当年的英国就像我们今天的中国一样，感情很容易受伤害，于是要求身为两广总督的叶名琛道歉，老叶据不认错，气得英国人只好象征性地往总督府打几炮，老叶龟缩不出，英人无奈就撤了。结果这个叶同志唆使暴民把广州的外国商行和外国人的居所一把火全烧了，你想想，凡是有点血性的民族会如何反应？ <br />
<br />
第二次鸦片战争，又称英法联军战争，这法国人又是如何被我们得罪的呢？因为我们没事把人家的一位叫马赖的神父给杀了。马赖是一位天主教神父，抛下在法国的优越生活，不远万里来到中国边缘落后的广西传教，结果被当地暴民和知县诬为煽动民众叛乱，判处死刑，受尽酷刑死后尸体被砍碎，抛给野兽吞噬，头颅挂于城外示众。马赖比中国人民津津乐道的白求恩早来八十多年，白求恩医治疾病，马赖拯救心灵，两人下场如此差异，怎不让热爱和平自由的法国人拍案而起，于是浩浩荡荡的英法联军来了。 <br />
<br />
英法联军先活捉了那个肇事的叶名琛，长驱直入攻下天津，一路清军兵败如山倒，如此不堪一击，搞的英法联军没有了兴致，签个“天津条约”，挥师南撤。此时，最愚蠢的中国人也应该暗自庆幸，趁机逃过一却，谁知第二年那帮愚蠢的家伙把前往通州谈判的英国特使巴夏礼等3９人全数拿下，关进牢里折磨致死其中的21 人，手法之残忍可与共产党折磨林昭，张志新媲美。看到四分五裂的尸骸，英军司令气的说不出话，不过他还是强忍怒火，深知北京市民是无辜的，作恶的是清朝朝廷，于是通情达理地一把火烧了皇家园林圆明园，不像中共陆肆屠城，不分老幼见人就杀。 <br />
<br />
每次望着圆明园的断恒残壁，我禁不住怀想，北京人民真是有幸，碰上的是英法联军，如果我们自己内斗，一方的同志被另一方砍成八块，还不来个扬州十日屠，在京城杀它十天？ <br />
<br />
说起甲午战争还真有点冤，属于朝鲜棒子捣乱，中国挨打，同毛主席的抗美援朝有点像。朝鲜原本名义上归中国，不时向中国进贡一点土产，后来日本势力增大，甲午战争前夕的朝鲜，基本上是中日平起平坐，双方旗鼓相当，天津条约约定“「将来朝鲜如有事，中日两国或一国要派兵，应先互行文知照；及其事定，仍即撤回，不再留防。」可是那些朝鲜棒子从中国人那里继承了捣乱的性子，不时搞点刺激日本人的小动作，日本人火气上来大军压境，那些朝鲜人连忙向清朝搬救兵，李鸿章搞了几年洋务以为自己强大了，屁颠屁颠派兵过去，三下两下稀里糊涂同日本干上仗，从平壤陆战到黄海海战，整个北洋水师沉到海底。 <br />
<br />
接下来就是八国联军。历史充满遗憾，如果当年英法联军一把火把紫禁城也烧了，烧得中国人心再疼一点，也许会收敛一点，那样就不会有后面的捣乱招来八国联军。义和团在朝廷怂恿下杀德国，日本的使节和其他西方商人牧师，八国联军出兵保护自己的侨民，师出有名，诚为正义之师。联军所向披靡，拳匪横尸遍野，这一战是中国人捣乱最彻底，挨打最舒畅的一次，里面的曲直家喻户晓，我就不多讲了。 <br />
<br />
最后不得不提的是七七芦沟桥事变，党一直骗你们是日军擅自演习，诡称士兵失踪要进城搜查，遭拒绝便开枪开炮，并且是蓄谋已久的全面发动侵华战争。可是战后远东军事法庭在所有的机密档案里没有找到日本策划芦沟桥事变的证据，而只有日本内阁对芦沟桥事变的惊奇和仓促应变的文件。当时的日军“天津驻屯军”是八国联军战争后各国留下保卫侨民的警卫部队，如果蓄谋已久要开启全面侵华战争，也应该调些精良部队。 <br />
<br />
