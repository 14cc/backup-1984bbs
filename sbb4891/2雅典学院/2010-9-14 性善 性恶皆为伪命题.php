													<h2>性善 性恶皆为伪命题</h2>
						
						
																											<div id="postmessage_829999" class="t_msgfont">性善、性恶皆为伪命题――兼与东海一枭商榷另一种人性观<br />
荆楚<br />
<br />
近段时间以来，东海一枭君颇为活跃。一会儿著文《信奉上帝是可耻的精神倒退》，一会而又赶制出《自由人士应接受性善论的指导》等等。反正是极力崇儒，并推销他的性善论。<br />
<br />
当然，在民主大潮的冲击下，东海一枭君所秉持的崇儒观点，自然是漏洞百出，弥缝不及。于是他儒、释、道并用，周易、八卦也派上了用场，阴阳五行也掺杂其中。一会儿道学，一会儿玄学。天文地理，雾里云端。无所不知，无所不晓。让人目不暇接、不知所云。<br />
<br />
我本来已经声言不再介入与东海一枭君的这种口水大战之中去了。但看到东海一枭喋喋不休于崇儒，到处宣扬性善之说，遗祸士林，谬种流布。且不时对基督徒进行漫骂侮辱。乃不避浅陋，斗胆一说。<br />
<br />
也因为性善性恶之基本持论，兹事体大。它关涉到许多基本的社会决策，也关涉民主宪政制度之建设。关涉法治环境之完善，更关涉到整个社会价值体系之构筑。<br />
<br />
确实，性善性恶，儒法两家争论了两千多年，谁也没能说服谁，也没有争论出一个结果。这是因为，无论性善、性恶，都是一个伪命题。双方都有证实的证据，但又不能否认大量存在的证伪之例证。<br />
<br />
按照基督文明所引申出来的基本观点：“人，一半是天使，一半是野兽。”在这种人性观的指导下，认为一种好的制度设计，一种好的社会价值导向，一种好的精神指归，可以把人诱导向化成天使。而一种坏的制度设计，一种坏的社会价值导向，一种坏的精神指归，则把人变成野兽。<br />
<br />
举一个例子，颇能说明这个问题：英国发现澳洲之后，于是将许多罪犯流放到那里去服刑。起初几次，当然是委托海运公司来承运。英国政府则按上船人数来支付运费。<br />
<br />
许多承运者只考虑多赚钱和多获利，而不管这些犯人的生活境遇。那时，运输速度还比较缓慢，从英国到澳洲一趟，一般需要一个多月，甚至几个月。在这种运输条件下，部分船只在运输犯人的过程中，死亡率高达2%。这样一来，英国人民舆论哗然，于是英国政府受到了各界人士的严厉批评。<br />
<br />
英国政府受到严厉批评之后，乃改变思路，将“按上船人数支付运费”改为按“按到岸人数支付运费”。<br />
<br />
经过这么一个小小“支付方式”的改动之后，犯人在运输途中的人道待遇得到大幅度的提升。基本上能达到100%的到岸率。<br />
<br />
当年的英国，是一个具有浓郁基督文明传统的国家。他们秉持基督文明之“人，一半是天使，一半是野兽”的基本人性观。在出现这个“运输事故”之后，他们没有偏执于性善与性恶之争论中去，而是秉持基督文明所引申出的人性观，并通过认真思考和分析，改进了技术手段，来规避资本逐利过程之中将“人性恶”发挥出来。而把资本逐利的本性予以利用，从而激发出“人性善”的成果。<br />
<br />
假如这种事情出现在中国，那么，人们理所当然地做出这样的反应――儒家性善论者坚持说，必须加强“政治思想教育”，必须强化“品行道德培养”等，来感化这些人不要那么昧着良心赚钱……而法家的性恶论者则反驳说：必须施以严刑峻法，必须严惩不贷，以便让承运人不敢昧着良心赚钱等等……<br />
<br />
两种思路，前者，日久必然流于虚伪蒙骗。后者，日久则必然流于血腥严酷。这都是刻舟求剑，缘木求鱼。其治理结果，也可想而知。就像中国历史发展的基本规律一样，只能长期陷入“治乱循环”之中而已。<br />
<br />
中国历代专制统治者，有单纯崇信法家的秦始皇，因其残暴刻深、嗜血寡恩，遂二世而亡。也有十分笃信儒家的王莽新朝，因其虚伪欺骗，唱行周礼，推广井田等，连他本人也不得善终。因此，无论崇儒或崇法，用其来治国理政，无一不是荒唐悖乱之极。<br />
<br />
倒是更多的专制统治者，吸取了这种深刻的“历史教训”后，秉持“内法外儒”的治国理念。或曰“儒表法里”的奴役控制之术。从而使得他们对社会的奴役控制时间要长得多。但这种表里不一的长期奴役控制，几乎使中国变成了一个两脚动物的丛林，使专制控制越来越严酷。使中华民族之思想文化和社会活力，越来越趋于窒息。只在春秋战国时代，有过一度的辉煌。只在民国年间，有过短暂的“复兴”。<br />
<br />
原因何在？在于秉持性善性恶之立论而作出的社会管理决策，是错上加错的结果也。所谓“差之毫厘、谬之千里”。立论的基础错了，岂有社会管理的决策能够顺畅的？<br />
<br />
而现代民主社会和文明国家所秉持的人性论，都是建立在基督文明所秉持之人性论的基础上。因而使得这些国家的社会管理、制度设计趋于科学化。从而暴发出巨大的社会活力。相对于专制落后国家来说，这些国家的文明与和谐程度，真是霄攘之别。从而成为追求文明进步的落后国家之精神向往，成为世界文明的价值导向。从而迫使专制落后之国家和地区的人们，苦苦思考迎头赶上的路径。<br />
<br />
不幸的是，中国共产党在国民政府与日本侵略军厮杀得气血亏虚、鲜血淋漓的时候，通过长期的与日军暗通款曲，养精蓄锐，乃在苏俄的秘密支持和指使下，暴力劫夺了国民政府之政权。毛泽东如愿以偿地夺得政权后，其魂萦梦系的，是做一个专制帝王。但鉴于袁慰亭复辟帝制的血泪教训，他只敢做一个超越皇帝之实的现代帝王，而不敢公开复辟帝制。<br />
<br />
毛泽东带着他的那帮泥腿子们坐上金銮宝殿之后，就像刘姥姥进了大观园。这些山沟沟里蹦出来的家伙，让他们干点打家劫舍的活儿，确实是驾轻就熟。让他们做些绑票勒索的勾当，确实是娴熟快捷。但让他们对儒教之思想根源进行剖析批判，则只能是――哈巴狗赶兔子――拿不到气。<br />
<br />
从表面上看，中国共产党确实发起过批林批孔的运动，但那仅仅是出于其内部权利争夺白热化的一种影射之术。在这种愚昧自负的驱动之下，他们只知道挖开孔子庐墓，把各地孔庙捣毁铲平，把各种文物古迹毁坏，以便将他们的兽性发作出来。因此，中国共产党批儒是假，崇儒才是其骨头里面的东西。因为儒教的专制理论伦理化，很适合作为中国共产党专制独裁统治的理论基础。<br />
<br />
而终马克思一生，则武断否认普遍人性的存在，强行把普遍人性装入阶级性的箩筐之中。这就更加不合逻辑、等而下之矣。<br />
<br />
在崇儒这个问题上，我希望东海一枭放眼看看世界文明发展的大势！再回顾一下人类文明迈进的脚步！而不是与中国共产党明里暗里之崇儒一唱一和。因为中国共产党为谋求长期的专制奴役统治，或明里褒扬支持，或暗中使劲崇儒，并试图把崇儒的事业推展到新的高度。<br />
<br />
再者，儒教向来提倡中庸之道。而基督文明的人性观，正契合于儒者之“中庸”观念也！既然东海一枭宣称自己崇信儒学，却又在这个问题上偏执于孔孟之“性善”一端。说明东海一枭的儒学功夫不到家，学艺不精。或曰只满足于跟着孔孟之徒鹦鹉学舌，只满足于拾取别人的牙慧，生吞活剥，猪八戒吞吃人参果。而不能举一反三，融会贯通，来把握精神实质。东海一枭的这些表现，连我这个儒教的半桶水，都感到捧腹的。何况那些饱学硕儒？<br />
<br />
也因为东海一枭崇儒之观点颇多荒谬悖乱，在与人的辩论过程中，往往理屈词穷。于是东海一枭虚晃一枪，乃一再感慨曰：语言文字的表达能力有限，不能表达出其内心深处的东西。一些心灵深处的东西，只可意会，不可言传。世人限于智识蒙蔽，难以领会云云。<br />
<br />
如果我遇到这种情形，我只能责怪自己的语言词汇不够丰富，责怪自己的语言文字的表达能力的欠缺，而不是反过来责怪世人之懵懂不开。<br />
<br />
既然东海一枭一惯崇儒，想必知道孔子之“推己及人”、“己所不欲勿施于人”，想必知道儒家之“日三省”功夫。但东海一枭的这番说辞，其弦外之音，不仅不是“推己及人”和“日三省吾身”。反而认为：当今之人，限于天资和慧根，限于智识蒙蔽，不足以理解东海一枭的复杂思想。也就是说：只有东海一枭最是聪慧明哲？其他的人，几乎都是一些二百五？<br />
<br />
以我之愚钝，领受如此指责，倒是没有多大意见。但泱泱士林之中，难道都是一些东海一枭眼中的二百五？屡屡如此说辞，也太自恋自负了吧！也太自我崇拜了嘛！而自恋自负和自我崇拜，在基督徒的眼中，是严重之罪恶。<br />
<br />
祈我主赦免他的罪恶！<br />
