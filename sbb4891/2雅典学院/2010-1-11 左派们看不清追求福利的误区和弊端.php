													<h2>左派们看不清追求福利的误区和弊端</h2>
						
						
																											<div id="postmessage_424989" class="t_msgfont">左派喜欢福利。一方面出于人性，白给的东西谁不喜欢？一方面出于善良――济贫。<br />
<br />
可是，追求福利有一个误区：在左派看来，好像用于福利的钱不是天上掉的就是政府的私房钱。殊不知，每一份福利钱都是纳税人的血汗！所谓福利，不过是把这些血汗由政府这个二道贩子收上去重新分配一下。羊毛出在羊身上而已。况且，由政府这个二道贩子重新分配本身就是强盗行为，凭什么我的血汗要抽去接济别人？你家对门住了个穷人，他有权成天要求你接济吗？没有。那凭什么政府就该有这个权？说穿了还是迷信理性作祟的结果，以为政府比谁都聪敏能干，能把抢来的钱分得更公平合理。可实际上由二道贩子去重新分配财富弊端很多。<br />
<br />
首先，被分配的钱只会越分越少，1，要给分配者本身开工资，这会消耗很多税款。2，什么人该分、什么人不该分，什么人多分、什么人少分，要甄别这些都要花钱，3，政府的低效率决定了一定会有浪费，4，还有贪污。<br />
<br />
其次，二次分配还会造成真正的不公。有关系的分的多，没有关系的分的少。不需要的可能分到了，特别需要的反而可能分不到。就像各地官员利用权力为自己和亲友分配经适房那样。<br />
<br />
第三，越追求福利，就越不得不赋予政府更多权力。政府权力越多，腐败机会越多。而且，你怎么知道政府没用这些福利款去建设金盾工程、安装绿坝呢？用于监督权力、防止腐败的花费也越多。财富就这么浪费了。<br />
<br />
第四，二次分配会打击勤劳者，鼓励懒惰者。勤劳者因为抽税而可能不再进取，生产和扩大再生产就可能萎缩，由此提供的就业岗位就减少，失业就增多，税收自然也就减少。懒惰者因为有白给的福利而进一步懒惰，但其对福利的指望却未必减少，反而可能增加，所谓奢侈的愿望。一个减少一个增加，最后，济贫这个初衷再也无法达到。难怪西方有句名言：左派同情穷人，却制造更多的穷人。<br />
<br />
那么，社会就不需要慈善事业了吗？非也。关键是要找准从事这一事业的合适人选――私人。经济学虽然假设了人的理性（私心），但并不完全排斥人的善性，斯密还写过《道德情操论》呢。以私制私，不仅可以抑制私，还可以激发善。饿死百万千万的大饥荒从来没有频繁地发生在资本主义国家就是明证。具体缘何？因为那里有私人慈善家（最初是教会）天天在活动。慈善事业不仅可给有钱人带来名誉上的满足，还因他们是私人行为，花的是私人的钱，会心疼，所以才更能体现经济和效率（花的钱少，办的事多、实）。而且，资本主义国家很多领域并无政府垄断，人们很容易找到适合自己和自己愿意从事的营生来养活自己，需要救济的人口本来就少。即便这样，后来发展起来的政府救济依然遭到诟病，原因还是如前所述。<br />
<br />
所以，政府的精力应当放在如何不妨碍人们自食其力（比如撤销或起码降低某些市场的准入门槛）上，而不是整天充当劫富济贫的伪慈善家！左派们也该透彻地看问题，而不要感情用事，充当无知的伪善家。唱高调不能当饭吃。<br />
