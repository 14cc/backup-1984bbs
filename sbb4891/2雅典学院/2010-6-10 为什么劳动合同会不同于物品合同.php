																											<div id="postmessage_672046" class="t_msgfont">第一，物品合同也包括长期合同和一次性合同的。劳动合同与一些长期合同类似，比如水费、电费、通行费等等，这些合同都有随着外部条件变化而内容（特别是定价）变化的特点。<br />
