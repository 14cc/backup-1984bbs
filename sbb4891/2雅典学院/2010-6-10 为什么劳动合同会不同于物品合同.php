													<h2>为什么劳动合同会不同于物品合同</h2>
						
						
																											<div id="postmessage_671896" class="t_msgfont">事实上,劳动合同是可以等同与物品合同的,,员工与企业之间,可以就每一个单位劳动时间如一天签定一次合同,更极端地,可以就每一个劳动行为签定一个合同,如挖一锄头签定一个合同,这样的合同和物品合同当然没什么不同<br />
<br />
但是,如果劳动合同是以一段时间内的劳务为标的, 情况就发生了变化,员工的劳动生产率会发生变化,与其他员工的配合熟练程度会发生变化, 当然,企业可以跟员工签定死合同,即薪酬并不随这些变化而改变,而作为回应,员工则要求岗位\劳动时间\劳动强度也应事先规定,如果是这样的死合同,和物品合同我觉得还是没什么不同<br />
<br />
然而,如果企业和员工达成的是薪酬浮动,岗位\时间\强度\小组等情况也可能会出现变化的合同,这个时候劳动合同就不同于物品合同了,企业拥有了管理权,拥有了合同所不能明细的薪酬和劳动条件决定权(我是支持企业调整乃至调低薪酬的权利的),与此同时,劳动者也当然拥有了跟企业要求更改薪酬和劳动条件的要价权,提出修改薪酬和劳动条件,对于企业和劳动者双方来说,都不是违约,而是一种事先就约定好的浮动空间.<br />
<br />
将管理权与变化条件相结合的长期劳动合同,混同于死合同\计件计时合同,甚至混同与物品合同,是一种思维的混乱</div>

							
							
							
							
