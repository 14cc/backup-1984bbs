第三，基于第二点，非垄断性的合同定价改变是双方的重新约定，期间，只要不违反原来规定，企业可以提高、降低薪酬，甚至解雇员工，同样，员工可以提出加减薪，也可以提出辞职。<br />
<br />
第四，以上这些是表面非常公平的合同内涵和运行机制，现在的问题是：看似公平的机制下面，企业和员工的话语权完全不对等。如何解决企业和员工的对等话语权，这是劳动合同要解决的根本问题。</div>

							
							
							
							
