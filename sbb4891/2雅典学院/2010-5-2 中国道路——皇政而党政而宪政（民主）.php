													<h2>中国道路――皇政而党政而宪政（民主）</h2>
						
						
																											<div id="postmessage_593607" class="t_msgfont">英美的宪政（民主）历史悠久，有一步步演变而来的传统。也正因如此，这两个国家繁荣而稳定，是治理国家的典范。<br />
<br />
中国自秦以来的2000千多年都是皇权至上，可谓皇政。也就是说，中国的传统是中央集权。<br />
<br />
辛亥以后的军政、训政也都有中央集权的实质。蒋经国后期才开始的宪政（民主），至今也问题多多，恐怕不能代表中国大陆也能自生、突生同样的体制，因为台湾还有50年日本统治的历史以及美国的影响。<br />
<br />
中共掌权后，皇政、军政、训政被党政所取代，当然也是中央集权，前30年甚至是中央集权的集大成者。<br />
<br />
革命就别想了，民主也暂时不能想，一方面是因为它们有太多条件制约，另一方面是因为它们本身也有太多弊端。<br />
<br />
唯一的可能是如何从党政中生出宪政――制约执政党的权力。这才是现实主义的办法。马基雅维利从自上而下的角度阐释了政治现实主义，我们何不自下而上地运用现实主义？<br />
<br />
中国目前的现实主义必须考虑几个方面：<br />
<br />
1，中共的党政制有其历史渊源（以皇政为标志的中央集权的变种），不妨认为其“合法”，起码是合传统的。<br />
2，中共自身也在努力约束自己的权力，尽管不如人意，但这种努力是不能视而不见的。<br />
3，30年改革开放造就了一批有产者，即使尚未明着喊出“无代表不纳税”的口号，但其对中共的制衡作用已经隐约可见。<br />
4，即使这种制衡是权贵之间的，也是有益的，况且它很可能是中国走上宪政所必经的。<br />
5，最关键的一点是，必须认识到：宪政（民主）不可能一蹴而就，英美况且如此，有着2000多年皇政传统的中国尤其如此。<br />
<br />
我以为，皇政而党政而宪政，可能就是可见的将来的中国道路。至于纯粹的民主，等宪政以后再说吧。</div>

							
							
							
							
