有关厦门海沧PX项目，曾有百名政协委员联名上书建议迁址(被称为“全国政协头号提案”)，可没用，该市市委书记说，“统一思想认识，委员提他们的，我们不理睬，要抓紧速度干。”政协委员的肝胆相照没能换来荣辱与共。此一悲。 <br />
<br />
<br />
正常的参政议政途径走不通，只好借助“第四势力”――媒体。遗憾的是，披露这一项目危害性的《凤凰周刊》被收缴，表达市民反对声音的论坛被关闭、短信被屏蔽。这不仅是在制造千万个编写短信针砭时弊的秦中飞，还可能埋下把人们逼上街头的隐患！此二悲。 <br />
<br />
<br />
整个事件发展过程中，只有市委、市政协、媒体和市民的声音，唯独听不到宪法规定的最高权力机关――人大的声音。在事关厦门全市人民切身利益的关键时刻，人民代表竟然“集体无意识”，当初投给他们的选票变得一文不值！此三悲。 <br />
<br />
<br />
收缴杂志、关闭论坛、屏蔽短信，当地公权力是何等的“神通广大”！这些暂且不说。这个遭到当地群众极力反对的项目，居然很有可能获得通过！这不能不使人产生如下联想：莫非又找到了一个郑筱萸式的人物？此四悲。 <br />
<br />
<br />
当然，谁都可以说，采取“不理睬”态度是对“百万市民”这一数字有质疑。可是，电信部门在大赚短信费时可以计算得那么精确，却没有在这么重大的问题上计算发信人数，为决策者提供民意“测验”结果，反而屏蔽了短信内容！这不仅是对公民通信自由权利的剥夺，而且已经在某种程度上佐证了发信人数之庞大。屏蔽短信就是拿庞大当乌有。此五悲。 <br />
<br />
<br />
好在如今是互联网时代，地球村任何一个角落的声音，都可通过手机按键和电脑键盘传得更远。厦门人用拇指挪动出来的“短信干政”，虽然已被公权所阉割，但这一无奈发明本身，又是一大喜讯。它清楚地告诉人们：只要是人民的声音，任何力量都无法完全阻隔。至于“短信干政”的效用究竟如何，就让我们拭目以待吧。<br />
<br />
――――――――――――――――――――――――――<br />
<br />
权力经济的荒唐与可怕<br />
<br />
南方都市报深度报道《南街村上演股权激励 红色亿元村神话或终结（上）》，戳穿了一个谎言，揭下了一张画皮，破灭了一个神话―――原来，支撑“共产主义小社区”南街村的，是两个能量巨大的“隐形外援”：巨额银行贷款及大量廉价外来劳动力。<br />
<br />
曾几何时，“南街村”这三个字多么光鲜，南街村人的生活多么诱人。“铺天盖地的宣传，几乎主要是两点：南街村选择了集体经济；更为重要的是，他们坚持了‘毛泽东思想’”。如果注意观察南街村发迹的时间，你会发现，1990年，南街村得到中国农业银行总行巨额贷款；河南“各级领导、各级职能部门都想抓南街这个典型。所需款项都是国家拨款，一拨就是几百万元”（南街村书记王宏斌语）。在邓小平“南巡讲话”后，尽管南街村宣称的起家资本“集体”和“毛泽东思想”，根本不能与当时的市场氛围相适应，但它依然在“个别人的肯定和支持”下挺了过来。可见，巨额银行贷款及大量廉价外来劳动力这两大“隐形外援”，只是两只有形之手，真正树起“共产主义小社区”南街村这块招牌的，纯粹是一句话、一个命令、一次会议就能拍板的政治权力。<br />
<br />
政治权力何以能够调动如此巨额的资金，去将一个乌托邦塑造为虚而又实、实而又虚的存在呢？是所谓的“公有制”！公有制除了计划经济这个表面运作特征以外，其核心的运行机制是政治权力的操纵。所以，计划经济也可以称为权力经济或命令经济。南街村发迹之时，中国的银行依然是完整的全民所有，其官员都是政府任命的。正因如此，这些银行官员不可能不服从政府官员的命令而按照“利润管理”的要求去放贷。这样一来，为政府的政治目的或官员私人的目的而设立的实体提供源源不断的资金供应，当然就成了银行官员保位升迁所必须完成的重要任务，为银行本身盈利的考量自然就得退居次要。公有银行的大部分呆账死账坏账，都是这么造成的。用于南街村造神而导致的巨大经济损失，只是这种体制的一个小小缩影而已。<br />
<br />
当今中国又处于一个十字路口，一方面，有不明就里的民众呼吁政府收紧管制，一方面，也有妄图回到改革开放前的权力运作（南街村的“兴起”及其至今死而未僵就是明证）。此时此刻，南方都市报及时推出揭露南街村真相的报道，实在是为我们提了个醒：以政治权力操纵经济运转会造成多大浪费！以集体、道德为组织和管制模式的经济是多么荒唐可笑！人为造就的乌托邦对公民自由权利的侵害是多么可怕！<br />
