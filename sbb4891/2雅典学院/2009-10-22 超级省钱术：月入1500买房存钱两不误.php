目前，正值“80后”进入结婚高峰期，但对于进入社会不久的80后打工族来说，应对高消费的生活并不容易，开源节流是唯一的选择。一名月薪仅1500元的80后小伙小仇，晒出他的超级省钱术，用合理的收支规划打理小日子，结婚、恋爱、娱乐、存钱样样不误。<br />
<br />
<br />
 <br />
　　主人公档案<br />
<br />
　　小仇<br />
<br />
　　男，二十八岁，未婚，<br />
<br />
　　在某国企任职，<br />
<br />
　　月薪一千五百元，工作稳定。<br />
<br />
　　有房一套，面积七十多平方米。<br />
<br />
　　小仇节约语录<br />
<br />
　　●钱是挣出来的，也是存出来的，如果你有一笔钱，不要去买时髦消费品，而要攒起来。<br />
<br />
　　●要习惯刷公交车卡，不要习惯刷信用卡。<br />
<br />
　　●心态决定一切，不要为了撑面子大手大脚乱花钱，真正的朋友是理解你的处境的。<br />
<br />
　　刚毕业的学生往往期望值高，但小仇在就业形势严峻的情况下，选择了“先就业后择业”的方式。虽然月薪仅有1500元，但工作稳定，他开始有条不紊地规划人生道路。<br />
<br />
　　吃<br />
<br />
　　自己下厨一周仅花50元<br />
<br />
　　每月扣除1000元“房奴费”，小仇的手里只有500元，如何合理分配这500元呢？小仇认为，最主要是要严格控制购物、合理安排饮食。“人是铁饭是钢”，对于一名男士来说，通过缩减饭量来节流显然是不可行的，但自己下厨却是个好办法，节俭且健康，居家男人还可讨得女士欢心。每逢周末，他就到超市将一周需要的蔬菜、肉类、馒头买齐，总金额控制在50元以内，零食这类可有可无的食品则坚决杜绝。<br />
穿<br />
<br />
　　衣服无损绝不另外添置<br />
<br />
　　男士对服饰的需求一般都没有女士高，因此在服饰上的开销可以尽量缩小。小仇认为，衣服贵精不贵多，只要每天保持衣服的整洁干净便可。在一件衣服外观没有明显受损的情况下，绝不额外增添类似的新衣。逢年过节，各大商场必搞促销活动，此时他并不急于抢购，而是先周旋于各大商场，找准折扣最大的商场再下手。<br />
<br />
　　行<br />
<br />
　　提早退场尽量赶搭公交<br />
<br />
　　一个月下来，小仇的工资基本就被吃穿住几方面“瓜分”了，但他还需要娱乐等其他应酬支出，以及存款用于将来结婚之用。这部分只能靠年终奖、过节费和办公室分红的盈余了。工作之外，小仇还寻找一些兼职，每月收入从500到2000元不等，谈恋爱、添置家具就全靠这笔钱。<br />
<br />
　　对于一些娱乐消费活动，小仇也是有选择地参加，严格遏制不必要的娱乐消费。就算有人请客，他也赶在公交车收班之前礼貌退场，一则可以免去打的费用，二来也安全。<br />
<br />
　　住<br />
<br />
　　家徒四壁先添二手家电<br />
<br />
　　工作稳定后，第一件事是考虑买房，和大多数80后一样，存款不多的小仇让父母帮忙先付了7万元的首期，为了不让自己承受太大的压力，他选择了每月700元，共20年的漫长月供。加上每月300元左右的水电费和物管费，每月1000元成了他雷打不动的硬性支出。<br />
<br />
　　虽然有了房子，但简装的新房，简直就是“家徒四壁”，家里只有一台大学时用的电脑和一张床，连电视机都没有。如何在不影响生活质量的情况下为小窝添砖加瓦？小仇先从购买二手家电入手。<br />
<br />
　　小仇在小区论坛上看到有邻居处理旧电视，和邻居沟通后，邻居将这台音响有点问题的电视机免费送给了他，只花了30元的维修费用，这台旧电视机就陪了小仇好几年。后来，单位的宾馆处理一批9成新的彩电，小仇及时出手，以100元的价钱买了台25寸彩电。这样，卧室和客厅的电视机都配备齐全。（记者 詹丽冬）</div>

							
							
							
							
