													<h2>从1984看TG能否退出历史舞台</h2>
						
						
																											<div id="postmessage_298292" class="t_msgfont">85年我去教堂过圣诞时，对基督教已经了解多年。但也正因那次经历，我走上另一条道路。 <br />
<br />
当时我抽到一帧签，签上有“把你的重担交给耶和华，她必叫义人永不动摇（？）”字样（前半句记忆无误，后半句不确定）。我想，把自己的重担交给神，那我还有什么用？！要知道，那会儿我早已是坚定的某某分子，一心想着灭了它。而且，我曾相信“知识就是力量”（培根语，我大学时外号就叫“培根”），曾发誓读尽商务出的书，也确实“博览群书”过。结果是，太过相信理性的作用，相信人的作用。有这样的知识背景铺垫，那个签就让我彻底放弃了信主的打算，并于陆肆后独自赤膊上阵，结果是“冷冻”一年零八个月。 <br />
<br />
我一早就不是唯物主义者（但至今也无任何宗教意义上的信仰，好像还在找，大概不久的将来会皈依基督教）。不管怎样，最近几年来，我对知识本身的作用产生了更多怀疑。也就是，不十分相信（不是完全否定）理性的作用和人的作用。这更加剧了我对唯物主义及其史观和辩证法固有的反感。我越来越倾向于相信有某种“天力”在主宰万物（尽管可能不一定是上帝、安拉或其他）。而且，我从奥地利学派、保守主义那里学来的自以为（或他们以为）真理的东西，用我自己的话来说就是：世界是大致的，错误是不可避免的，代价是一定要付出的，能达到让人可以容忍的程度，就是可以接受的，最终渐变到大致美好。 <br />
<br />
当然，现状是不可接受的，已经变化或按照当局主导的即将来临的变化，其代价也大得无法容忍。所以，另外一种变是理所当然的。但如何“另外”地变？靠曾经荒谬绝伦的“人定胜天”显然不行，唯物主义、唯理主义当然也不行。遗憾的是，从1984不少跟帖接受当局对某唯心主义组织的邪教定性来看，从一些人对资本家追求利润和对市场机制的仇视来看，从某些知识分子自以为掌握了绝对真理从而觉得可以“在一张白纸上画最新最美的图画”的傲慢来看，从很多年轻人对传统文化依然嗤之以鼻的态度（我曾经也是如此）来看，他们还在（其实是不自觉地企图）依仗TG曾经依仗的物质和精神“法宝”来改变现状。 <br />
<br />
尤其是，有那么多支持政府干预经济（从而等于允许政府扼住经济咽喉）的人在，有那么多看不到扼住经济咽喉也就可以扼住其他的人在，TG是退不出历史舞台的。如此，在我看来，即使TG阴差阳错地退出了历史舞台，新的掌权者仍有可能重演社会主义的悲剧，中国离英美的自由民主就还差得很远很远。<br />
