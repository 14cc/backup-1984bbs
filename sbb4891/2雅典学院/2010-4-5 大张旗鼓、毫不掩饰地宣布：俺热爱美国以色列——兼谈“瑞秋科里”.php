																											<div id="postmessage_548587" class="t_msgfont">大张旗鼓、毫不掩饰地宣布：俺热爱美国以色列<br />
<br />
在转贴的《敌人的权利》下边，俺对那些以不实之辞攻击以色列美国、以色列者给予了回击，那篇刘瑜的文章，从美国处理关塔纳摩事件入手分析了美国强大的纠错机制。如果对应“瑞秋科里”事件这种回应是间接的话，俺会在本文最后分析一下为何关于“瑞秋科里”的文章充满不实之辞。<br />
回应某个质问――恁雷文1970现在已经是“自由民主教”啦，就跟从前的共产主义教一样，从信奉共产主义，发展到盲目信奉苏联，盲目信奉毛泽东，恁现在不过是盲目信奉美国而已。发出这种指责的人俺不想提了，一来俺写这些文字是对事不对人，二来早在去年看此君在论坛上的跳梁，实在是不值一提。<br />
首先俺想如标题那样承认――俺就是热爱美国，大张旗鼓、毫不掩饰地宣布：雷文1970就是热爱美国，还包括以色列。<br />
建议：热爱美国的朋友，不要羞羞答答，不要掩饰自己，我们都应该勇敢地宣布自己内心的真实感受：我们热爱美国，热爱这个星球上罕见的正义民主自由之国，地球上有美国，是地球的福分！<br />
美国之所以值得热爱，我们都可以想到很多很多的地方，就不提了。单说热爱美国这种感情好不好？是不是就像当年共产主义者热爱苏联和毛泽东一样？<br />
要俺说，热爱美国这种感情好得很！热爱美国，就是热爱一种人人享有公平机会的生活，就是热爱一种勇于承认、揭示、纠正自我错误的态度，就是热爱一种充分保障个体公民权利的制度，就是热爱一种有效限制公共权力、国家权力，使之更好服务于国民的先进制度。这种感情，是最正常的，最健康的，最属于基本人之常情的。<br />
有人说，恁们这些家伙，从热爱自由民主开始，到热爱一个具体国家，凡是关于这个国家的坏话，都说不得。实在是盲目啊非理性啊、神经错乱啊！<br />
好像是俺颠倒了次序，本末倒置，首先应该热爱的是“自由民主”而不应该是哪个具体国家一样。实则这屁话。人的正常健康情感，都不是抽象的，俺当然热爱“自由民主”，但俺热爱的不是抽象的自由民主，而是实实在在的、在人类存在的某个地区出现、并给这一地区人类带来不可辩驳、不容置疑福祉的自由民主。是先看好这种实践，才热爱“自由民主”的。而不是相反。<br />
换言之，俺是先对保障具体而微的、实实在在的，生动鲜活的人权、公民权非常得力的美国有好感，俺才对美国建立的制度、奉行的准则、遵循的信条有好感的。热爱民主自由，根本不是什么玄而又玄的先验的东西。就是说，热爱美国是在先的，我们热爱美国那种拯救大兵瑞恩的行动，我们热爱美国对水门事件、拉链门事件穷追不舍，我们热爱他对于极权国家的威慑和抨击，这才召唤我们热爱美国制度，热爱民主自由。<br />
某些人，可能说他是五毛有点冤枉他。他对中国的现实也反感，也自认为热爱“民主自由”。但他的民主自由倒是他自己抽象想出来的，根本就没有现实对应物，他热爱的与其说是民主自由，莫若说是他自己――他自己虚构出来的一种谁也了解不了、高深莫测、在地球上哪怕是美国也没有实践的一种原则、一种制度。爱，不是从具体的人事中，不是从具体的感受中得来的，这种爱跟自渎差不多。俺认为这样的人根本没有爱的能力，他们只是在“理性”、“深刻”、“辩证”幌子下的冷血动物而已！<br />
身在极权下的人们，没有被洗脑，没有麻木，尚且对自由的生活有着强烈向往而不可得的人们，他们对自由国家的向往，东欧作家凯尔泰兹在《英国旗》中表现得淋漓尽致。在没有言论自由的时代，没有安全保障的年代，随时可能被传唤、被消失的岁月，街上飘过的一面英国旗，便足以让受尽侮辱与损害的人热泪盈眶，从而战胜绝望。<br />
――俺像凯尔泰兹先生这样大声宣布：<br />
我爱美国，英国，以色列，我爱星条旗，我爱米字旗，我爱大卫星旗！<br />
让冷血动物、让犬儒、让故弄玄虚的深刻见鬼去吧！<br />
这种热爱，根本与左翼运动分子对苏联的热爱，对领袖的热爱是两回事。冷血动物注重的只是形式，从形式上看，似乎差不多，实则根本不一样。正如哈耶克在《通往奴役之路》中所说的，左翼运动实则是一种集体主义运动，它的根本方法是消灭个体性，让个体融入“集体”运动中，丧失自我，丧失人性，陷入迷狂。而对美国以色列这样的国家的热爱，纯然是从个体感受、个体最正常感受出发的，是最体现人性的。别的例子不多说，俺附录一篇近日所写的一篇关于以色列政府与卡夫卡手稿的文章（《卡夫卡九泉之下也会感谢以色列》），俺从以色列政府在追索国宝（卡夫卡手稿）中对待个体公民的态度，可以看到这个猎杀恐怖分子如探囊取物的强有力国家，在公民权益面前又是何等“软弱可欺”，再反观天朝的历史和现状，俺对以色列的热爱油然而生。这是最正常的人性、人的情感、个体感知的体现，何来盲目之说？假若我们都“回到你自己”，尊重个体的判断和自由，任何反个体、反公民、反人性的行径和观念、意识，都会遭到质疑和否定。换言之个人主义出发的自由主义本质上拥有一种理性精神，拥有对狂热、极端、盲目等等病态情感的解毒和过滤功能。假若美国、以色列犯下过错或罪恶（这并非不可能，除了上帝或圣人，都会出错），正是从这种个人主义产生的思维、思想、观念、舆论、制度，保障了他可以有效纠错。（沃农&#8226;路易&#8226;帕灵顿《美国思想史（1620～1920）》中，有专章论述个人主义作为美国思想的根本。个人主义是个非常大的话题，从略。）<br />
冷血动物们从来不从活生生的个人感受出发，而是从抽象的形式出发，对事物进行自以为是的类比。俺也可以拿冷血动物的逻辑这样反问：<br />
你开头是热爱女人的，从而热爱你女友，想不到后来竟本末倒置，热爱起后者来了，谁说个不字都不行，这岂非岂哉怪也？！<br />
。。。。。。。。。。。。。。。。。。。。。<br />
假若说对美国以色列英国这些国家的制度之批评，够客观，够见地，俺是绝对不会徒作无聊辩解的。关键是第一，不要像典型的五毛党那样，一而再、再而三、黔驴技穷一样老拿人家早已解决了的问题来说事。比如黑人，比如印第安人，比如关塔纳摩。那不仅没意思，反而最终会成为美国了不起的又一个证据，还是歇歇吧，五毛党为了那点银子，也情有可原，但其他自认不是五毛党的就有点不可思议了。第二，真正有见地，二把糙就免了。我看对西方制度批判有见地的，还是西方人，比如法兰克福学派这些人，整天在国内享受着吃屎的待遇，去品评红烧肉之副作用，或者在美国根本融入不了主流社会，对美国根本一知半解，就一副酸溜溜的样子大骂人家，这都啥德性啊？<br />
俺说这些文字大多垃圾，谓余不信，那就分析一下最近1984这个帖子――《看看自由民主的明珠以色列是怎么搞拆迁的：纪念瑞秋科里》。<br />
这是一标准的扯淡帖子。<br />
为啥这么说？以色列推土机轧死一个“和平抗议者”，俺也觉得这抗议者的确值得同情。再加上该文渲染说以色列不了了之，美国也没啥动静，似乎就坐实了民主国家虚伪的真面目。<br />
但是这篇文章的立论基石，应该是这样一个重要事实，即瑞秋科里当时保护的不是恐怖分子的活动场所。可惜的是，这个证据始终付之阙如。尽管文章绕来绕去，但没有任何证据用来说明，瑞秋科里不是在袒护恐怖分子。相反，通过这篇文章很不屑引用的资料，我们反倒看到，《纽约时报》报道她所保护的巴勒斯坦医生住宅藏着恐怖分子运送武器的隧道。<br />
在没有任何其他佐证的情况下，我们是相信巴勒斯坦方面的说法，还是相信《纽约时报》的说法？我看这就像相信TG说法或相信美国媒体一样，这是一个道德问题，这更是一个智商问题。即便不相信《纽约时报》的报道吧，那么该文也根本没有任何证据来反驳该报，但是这并不妨碍该文继续煽情地引用鲁迅文字――“流言家竟至如此之下劣”。的确，够下劣的，没有任何证据来反驳纽约时报，却根据需要悍然把人家定性为流言家，这才是真正的下劣。<br />
该文除了没有拿出任何证据就下结论、巧言令色，另一大特色，也是和五毛党一贯的不自觉思维习惯不谋而合。这一思维习惯是这样的：<br />
你以色列、美国不是民主自由么，不是充分保障人权么，面对和平抗议者你就应该保持克制、保持宽厚姿态么，无论她所保护的是哪个，无论是利用那座房子偷运武器再用武器谋杀平民的恐怖分子也好，是巴解组织也好哈马斯也好，反正你不是保护人权么，你咋能动起手来？动了手，就是你的不对。<br />
你以色列、美国都是民主国家，一旦发生这种事情，肯定得大动干戈，大规模调查，国会抗议，群众示威，这次居然没多大动静。这太不可思议啦，这说明你们的所谓权力监督是多么虚伪。。。。。。。。。。。。<br />
五毛党就是这样的，对小人，哪怕整天拆迁弄死人，一有个和谐拆迁，就感激涕零。对君子，则要求很高，哪怕没有任何证据证明她不是保护恐怖分子，也要振振有词、大义凛然、道貌岸然地质问人家为何不保护人权了。其实他们骨子里是相信以色列美国这种国家不敢冒然去伤害瑞秋科里的，我相信瑞秋科里也知道这一点，就是以色列军队一般不会野蛮地拆迁，否则，如果她面对的是日本鬼子德国鬼子，或者恐怖分子，或者天朝的拆迁黑社会，我倒真不知道有人还会不会真的挺身挡在铲车前。张平先生写的那篇《战争即地狱》里，那个恐怖头子其实也是这种心理，他不相信他及他的妻子、儿子在家，以色列会轰炸他的住宅。<br />
恐怖分子和五毛党最可恶、最卑鄙的地方就在这里：他们心里往往最清楚什么是文明，哪一方更文明，但为了自己的权力和蝇头小利，他们出卖自己的良心，还忽悠别人出卖良心。最近，俺看《拆弹部队》，觉得那些美国军人特傻，面对随时可能被引爆定时炸弹、粉身碎骨的险境，几乎从不首先、主动对周围可疑的、甚至强行闯入现场的人开枪。这些镜头、情节反复地展现。那些站在一边遥控的恐怖分子，也充分知道美军不会“宁可错杀也不放过”，所以大摇大摆，满不在乎。看着这些镜头――我不认为这是虚构的――我不禁想起了麦克凯的那句话――<br />
“我们比我们的敌人要好。”<br />
我热泪盈眶。就像凯尔泰兹那样热泪盈眶。在这样的地方飘扬的不是米字旗，而是星条旗。巴尔加斯&#8226;略萨曾说：“我们生在哪儿是很偶然的事情。但我们可以用我们的全副身心，为自己寻找一个心灵的祖国。”<br />
我认为，并且相信，星条旗飘扬的地方，就是我心灵的祖国。我爱这样的祖国，我为地球上以及我的心灵中尚且存在这样一片地域而感到庆幸，当然，犬儒和冷血动物永远不能理解这一点。<br />
<br />
