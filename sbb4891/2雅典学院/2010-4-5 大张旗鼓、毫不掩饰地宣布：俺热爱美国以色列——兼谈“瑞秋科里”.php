																											<div id="postmessage_548589" class="t_msgfont">附：老卡九泉之下也会感谢以色列<br />
 <br />
<br />
1.<br />
<br />
 <br />
<br />
卡夫卡还有许多未面世的遗稿，而且，这遗稿保存在以色列的两名白发姊妹手中，现在分成六箱，五箱保存在以色列银行的保险柜中，一箱保存在瑞士银行的保险柜中！<br />
<br />
――对于世界上无数的卡夫卡迷来说，从这个消息中得到的激动、兴奋，怕是完全可以和小孩子圣诞节从“圣诞老人”手里拿到一件礼物相比。而这个消息，是绝对真实的，英国《每日电讯报》近日对此作了报道。著名文学网站左岸也发表了国内记者的述评。<br />
<br />
对这个消息，俺也是难掩心中的快慰，当晚特意喝了一瓶啤酒。俺是个标准的卡夫卡爱好者，这可并非是萨克雷所谓的“势利鬼”，要攀老卡这个文学权贵。俺的卡夫卡爱好史，要追溯到刚上大学时代，自从看了《变形记》，便痴迷上了这个表面上过着保险公司高管生活、内心却敏感动荡充满罪感、感情生活痛苦不堪、只能靠文学来排遣对整个世界恐惧和荒谬感的孤独的老单身汉。想想看，他晚年患肺结核、白喉，生命垂危之际，还对医生写出“杀了我，否则你便是凶手”这样充满黑色幽默的便条，他的文字之怪诞，充满张力，完全像黑洞一样吞噬着任何接近他的人。<br />
<br />
老卡感情细腻，为女友们写了一本又一本的情书，现在中文译本有《致菲利斯》、《致密伦娜》。然而，由于女人们的功利，由于现实的钳制，也由于他过分的胆怯与优柔，他的感情故事无不以失败告终，临终才得到一个名叫朵拉的女子无私的看护与爱，然而他的幸福也只是生命尽头最后的半年时间了，而且这半年也是充满了病痛折磨。他的家人、女友、恋人，大多数在随后的十数年中惨死于纳粹集中营，完全应验了他的那些对世界、对极权充满恐惧的伟大现代主义作品的谶言。<br />
<br />
这是一个可以说生前悲惨、身后亦极其悲惨的作家。俺确实不知道那些在生活中左右逢源、其文字根本无关现实体制之荒谬和心灵痛痒的作家们，你们到底喜欢卡夫卡啥东西？是啦，但凡宣布，“我从卡夫卡那里学到很多”，“我继承了卡夫卡的衣钵”，便能陡然地从大师那里沾得很多荣耀，很多便宜，但是俺想说的是这份荣耀和便宜，是从卡夫卡那里偷来的。<br />
<br />
犹记得俺在上学时代以及工作后，如饥似渴地读着卡夫卡那些冰冷的作品，你很少能从卡夫卡那里获得暖意，但却可以通过获知一个人可以荒谬悲惨到彼等地步，来宽慰自己，因为你永远也变不了一只甲虫。总是在这样变形的恶梦以后，醒来时发现，那只是梦境或文学世界。卡夫卡生活在梦里，看这个梦，自己也就像是处于醒来后的早晨了。卡夫卡的作品，零散的，文集，散文集，俺几乎把能买到的都买全了。卡夫卡的传记，从布洛德的，到瓦根巴赫的，从海曼的，到日本人三野大木的，俺都拜读过，最喜欢的是瓦根巴赫的《卡夫卡传》，资料最为丰富翔实，最喜欢的书名却是三野大木的《怪笔孤魂》。。。。。。。<br />
<br />
教科书说，“卡夫卡是表现主义的经典作家”，“现代主义文学的鼻祖”。这个说法过于教科书化，也不亲切。俺个人的体会，从卡夫卡开始世界文学把人类体验的结构和表现形式重塑了，如果说现实主义表现的是“像”，而表现主义则是“是”，人像甲虫、地鼠，被人是甲虫、地鼠，所取代，其所开启的意义表现空间和体验方式，是全新的，颠覆性的，所以马尔克斯在18岁在波哥大大学的寝室里读了《变形记》后，把书一摔，宣布：<br />
<br />
“XXX（类似于“他妈的”的国骂），小说可以这么写。俺也会了。”<br />
<br />
卡夫卡启发了无数后世作家，许多诺贝尔文学奖作家都自认是卡夫卡的传人，例如卡内谛，库切。日本的存在主义作家安部公房，被誉为日本的卡夫卡，而日本存在主义流派的大江健三郎在获奖时曾坦承：这个奖应该给安部公房前辈。卡夫卡和托尔斯泰这样的经典作家未获诺奖，被认为是该奖项的世纪之憾。但是对于这类作家来说，获不获奖根本无损于他们的价值，而且他们也根本不关心什么文学奖授给了谁。<br />
<br />
 <br />
<br />
2.<br />
<br />
 <br />
<br />
老卡生前发表的作品并不多，只占他手稿的极少一部分。<br />
<br />
他在弥留之际，将全部手稿托于他的挚友马克斯・布洛德，要求全部烧毁，不留片纸。他为啥这么做？这对卡夫卡研究者是个谜，据俺猜想，首先是卡夫卡自己恐怕也并不是十分清楚他的作品的巨大价值，那时候占统治地位的仍是现实主义，像他这么写，当时完全是“歪门邪道”，他对自己的作品始终不是很自信，三部名垂千秋的旷世长篇（《美国》、《审判》、《城堡》），都是未完成的。其次大概也与精神世界有关，这些作品纪录的都是老卡最隐秘最深刻的心灵创痛，对他来说惟一的作用就是倾泻郁闷，他不愿呈示于世。卡夫卡曾写过估计是古往今来最长的一封信件――《致父亲》，长达2万余言（汉译），在其中他诉说了父亲一贯的专制粗暴，他在家庭中的“恐惧与颤栗”（借用一下丹麦哲学家、存在主义先驱克尔恺郭尔的书名，克也正是卡夫卡最为喜欢的作家之一），这封有着巨大文学价值的长信写完后，卡夫卡犹豫再三，徘徊再三，最终也没有将它呈给父亲。外在世界对他来说，也仿佛就是一个粗暴专横的父亲，正如隐藏《致父亲》，老卡的临终遗嘱，也是将作品永远地从世界中抹去。<br />
<br />
感谢布洛德，他终生都是卡夫卡可信赖的朋友，几乎惟一的知音，但在卡夫卡手稿问题上他作了一次伟大的背叛。用老卡的句式来说――“正因为我是你真正的朋友，所以我要背叛你的遗嘱。”<br />
<br />
布洛德将卡夫卡的许多手稿整理、编纂、出版，于是世人才看到了老卡的三部长篇，（其中只有一小部分，即《美国》的第一章，在卡夫卡生前以《司炉》为名发表过；不少部分都是布洛德在凌乱的草稿中整理出来的。）以及大部分短篇、散文片段。二十世纪三十年代以后，各种极权势力席卷世界，给全球带来了空前的灾难，给个体带来了身心的恐怖与毁灭，个体的“这一个”在庞大荒谬的现实尤其是权力面前显得如此弱小和无助，难以用启蒙哲学的理性来解释的侵害、受伤、剥夺时时袭来……卡夫卡的梦境世界变成了现实，他的恐惧成了世界性的恐惧，对那些灾难、怪诞、梦呓般的现实，人们试图解释的时候，发现卡夫卡早已对它们进行了天才般的预言和描摹，于是，卡夫卡的作品引起世界范围的轰动，“卡夫卡式的”被人们用以命名某些“超悲剧”境况，卡夫卡学在西方成为几乎与莎士比亚学同样重要的文学史显学……<br />
<br />
由于本身创作产量不高，且生前卡夫卡就开始烧毁一些作品，卡夫卡的问世作品就显得格外珍贵，几乎每一篇、每一行，对卡夫卡迷、卡夫卡专家来说都是珍品，都像是用文字凝成的珍珠、钻石。俺有本《卡夫卡随笔集》，收录了大量卡夫卡的断简残篇，写在小纸片上的格言短札，几乎每一则都是那样的耐人咀嚼，不仅是蕴含丰富，而且非常多义，几乎经得起无限的品读和诠释，能同卡夫卡相媲美的二十世纪随笔作家――不是单一的文学性比较，而是思想的深刻性、丰富性、多义性，乃至语言的独特性，全方位的比拼――俺自己觉得惟有两位作家：哲学家路德维希・维特根斯坦、罗兰・巴特。非常有意思的是，维特根斯坦也是一个终生的单身汉，他对现实成就和财富像卡夫卡一样漠不关心，曾把上百万的巨额父辈巨额遗产分赐给亲朋好友，和卡夫卡一样都是本领域圣徒式的人物。<br />
<br />
 <br />
<br />
3.<br />
<br />
 <br />
<br />
正因为卡夫卡的文字较少，极其珍贵，故尔他的大量从未公开的手稿有望问世，引起了全世界的关注和兴奋，也引起了一些卡夫卡爱好者、尤其是卡夫卡专家的愤慨。<br />
<br />
以色列特拉维夫大学的卡夫卡专家努里特・佩吉女士说：“这是四十年的愤怒。连看一眼都不准，尤其是一个女人打着保护卡夫卡的旗号。”<br />
<br />
要理解大家之所以极其愤怒，还要讲一下卡夫卡这批手稿命运的故事。一句话，这些珍贵的手稿在长达四十年的时间中，被一个名叫埃丝特・霍费的女人所把持，恐怕除了少数文物商人，以及曾经在霍大妈漫不经心存放手稿的破阁楼上漫步、便溺的几十只野猫，其他任何人都不曾被允许看过这些伟大的人类遗产哪怕一眼。<br />
<br />
卡夫卡逝世后，手稿由布洛德保存。布洛德整理发表了一部分手稿后于1968年在以色列辞世，手稿便落入了他的秘书霍大妈手里。从此，以色列政府便陷入了与霍大妈长达四十年的手稿争夺战中，前者希望后者将手稿卖入国家图书馆归档，后者坚称这是布洛德交给自己的遗产，任何人无权征用。<br />
<br />
要知道，正像许多世界伟人，卡夫卡也是犹太人，他的著作是全世界犹太人、更是以色列的骄傲，以色列政府为了得到这批无价之宝想必绞尽脑汁，无奈，霍大妈头脑里就是没有国家、民族荣誉这样的概念，坚决不答应把手稿卖给以色列图书馆。布洛德在地下有知恐怕会非常后悔自己把好友的手稿竟然交付给了一个财迷和市侩，有一次，以色列警方在特拉维夫的古里安机场检查霍大妈的提包，从中查获一些卡夫卡信件和旅行日记手稿，但也只能按照有关文物管理规定禁止霍大携带出国，只能使得她同意档案机关给手中的宝贝编目，但这些手稿仍在她手中，而且外界高度怀疑她将许多最珍贵的文稿秘不示人。<br />
<br />
全世界都在争夺这笔文学遗产。霍大妈和以色列政府斗智斗勇，力争实现利益最大化。1988年即布洛德交付她手稿20年后，她终于得手一次，通过索斯比拍卖行以110万英镑的高价将卡夫卡的名著《审判》手稿卖给了一名书商，但幕后的买家实际上是德国政府。卡夫卡是德语作家，所以德国、奥地利这些德语国家把收藏卡夫卡手稿视为理所当然。<br />
<br />
即使到了辞世之际，霍大妈仍对卡夫卡手稿不松手，她又将它们传给了两位女儿，就是埃娃・霍费和露特・魏斯勒姐妹。<br />
<br />
终于熬了40年等到霍大妈去世，以色列政府岂能再坐等这俩女人把手稿零敲碎打卖出去，终于起诉到法院，法院近日已经裁决，将这些手稿收入保险箱存放于银行，使它们再也没有遭受野猫便溺之虞；15日内两姐妹必须与以色列国家图书馆达成收藏协议，否则将强行打开保险箱。其法律依据是布洛德虽然在遗嘱中把手稿交付霍费秘书保存，但其目的不是让她私人占有，而是最终收入公共档案馆。<br />
<br />
《每日电讯报》的报道是在1月21日，相信不日卡夫卡的手稿就可以找到一个合乎布洛德意志、合乎全世界美好愿望的归宿了。<br />
<br />
 <br />
<br />
4.<br />
<br />
 <br />
<br />
《每日电讯报》对这批手稿命运及这些“不负责任的老太太”的披露，恐怕不仅让佩吉这样的卡夫卡专家愤怒，也让普通读者愤怒不已。<br />
<br />
执拗、顽固和贪婪，使卡夫卡的许多私档及未完成作品40多年不为人知，而本来它们是可以早日见到天日的。这一点即便连纳粹也做不到，当年纳粹兴起，身为犹太人的奥地利作家布洛德携带卡夫卡手稿逃到巴勒斯坦，不是也整理发表了大量手稿吗？而霍大妈及两个女儿的行径，让“卡学”某些领域“学问基本靠等，研究基本靠猜”，这是何等的造孽、何等的愚蠢、何等的不道德啊！<br />
<br />
――乍看报道，俺也是这么想的。如果霍大妈不是这样的造孽、愚蠢和不道德，俺们这些卡迷不是早就读到了卡夫卡的许多新作？<br />
<br />
怒火中烧之际，俺突然打了一个激灵。因为头脑中不期然而然产生的一个愤怒的问题，让俺对自己的想法产生了警觉。这个在义愤推动下发出的质问是：<br />
<br />
为啥以色列政府不强行收走霍大妈手里的手稿？！这岂非易如反掌？何必苦等40年？<br />
<br />
唉，唉！俺承认自己队整个事件的看法，已经不自觉地陷入了某种专制思维中！以色列政府的做法根本没有错；整个手稿事件也根本不是一个“杯具”！<br />
<br />
是的，在某些国家和地区，这种“荒谬绝伦”的事情根本就没有发生的可能――从霍大妈手里没收手稿，简直太容易啦。不要说强行没收还有“支持卡夫卡研究”、“挽救卡夫卡手稿”、“挽救以色列文化荣誉”甚至“保护世界文化遗产”这样冠冕堂皇的公益性理由，就算没有任何理由，在我们国家的某些地区，随意收走公民财产不也是曾经发生、并且正在发生的日常故事吗？<br />
<br />
在“文革”等荒唐岁月里，公民的家庭可以被随意查抄，财产可以被随意没收。<br />
<br />
在今日，地方政府和无良开发商，随便编个公益借口，就可以随意拆迁公民的房产，公民的私权还没有、甚至远未得到必要的保护。<br />
<br />
“霍大妈把持卡夫卡手稿40年，而政府无可奈何”的故事，在这样的地方是绝无可能发生的。它们肯定早就被查抄走了（很不乐观地猜测一下：查抄走后，它们也多半被卖到废品收购站、最终化为纸浆了）。<br />
<br />
但是在以色列，霍大妈的破房子却成为她抵抗政府意志的坚固堡垒，40年中，无论多么想得到手稿，多么处心积虑，多么严阵以待，以色列政府始终不敢迈进霍大妈的房门一步，更别说强行将手稿抄走。即便在机场查到她私自携带手稿准备外出，也只能是将手稿截留下来，护送回霍大妈家了事。<br />
<br />
归根到底，手稿是布洛德遗留给霍大妈的，已经成为她合法的私人财产；布洛德指出手稿应当最终放置在公益档案馆，那么霍大妈死后她就无权支配这笔财产，但她活着的时候，只要她不违背这一遗嘱，他人就无权动手稿一指头，而只能与她平等协商。假若她不愿意达成合作意向，那么以色列政府无能为力。<br />
<br />
这也就是以色列政府、以及公众必须等到霍大妈去世，才能拿到手稿的法理学根据。40年，十分漫长，但必须等待，因为在永恒的法律眼中是没有时间概念的。<br />
<br />
令人感动、感慨的是：任何理由，无论多么“宏大”、“崇高”、牛X――比如保护卡夫卡文学遗产、拯救国家荣誉、为了公众需求……――都不能撼动合法的公民权利之分厘，不能损害合法的公民权利之毫毛。<br />
<br />
我们通过这件事可以体会到，在以色列，个体公民权利高于一切。在保护卡夫卡手稿和维护公民权力之间，宁可放弃前者，也要保存后者。<br />
<br />
这是何等的大气和牛X啊！什么国家荣誉、文化遗产、公众需要……如果这些东西需要以损害公民的合法权益为代价，那么对不起，请你们给公民权益让路，无论这个公民是多么的普通、卑微，甚至顽固、蛮横、愚蠢、不道德，这都不能成为国家、公权剥夺她（他）合法私人权利的借口。<br />
<br />
这样的国家看似“软弱”，看似“低效”，实质上却非常强大，因为真正的强大是来自法制、来自公正、来自对人的自由的尊重。<br />
<br />
这样的做法看似有损卡夫卡的遗产，却是对卡夫卡最大的尊重。因为卡夫卡最为恐惧的就是肆无忌惮和荒谬绝伦的权力对个体的蹂躏与摧残，他的手稿经历的故事，生动地证明，他的民族在历经磨难之后所建立的国家，是一个有别于他为之恐惧与颤栗国家的自由国度，也是他梦寐以求的地方。如果在这样的地方，权力可以打着保护卡夫卡的旗号来剥夺公民财产，那是对卡夫卡最大的不敬和侮辱！<br />
<br />
这样的做法看似损害了国家利益，却本质上是对国家利益的最好捍卫。想想看，一个国家，哪怕出自最堂皇的理由，也不能被作为剥夺公民权利和自由的工具，那么，公民有什么理由不爱她？！“连国旗都可以烧，美国人有什么理由不爱美国？”这是美国人的自问，以色列人也可以说：“连卡夫卡的手稿以色列政府都得不了手，以色列人有啥理由不爱以色列？”<br />
<br />
是的，这样一个国家可以派出猛士在敌国的土地上虎穴救人，可以数十年如一日全球定点清除一个个纳粹余孽，可以用精准到厘米的武器秒杀恐怖分子，但是，它却做不到攻陷本国国土上一位老太太的破房子和破箱子。而这正是这个国家最可怕的地方，是它在强敌环司中生存和发展的生命之源。</div>

							
							
							
							
