													<h2>个人对六四事件的看法</h2>
						
						
																											<div id="postmessage_595452" class="t_msgfont">对于六si事件相信在1984的人都有所了解，那些具体的什么领导内斗，压制民主之类相信大家都说过了<br />
那我就单纯不带感情地说一下个人对六四的看法。<br />
<br />
个人觉得，六四的背景，发展，高潮这些过程都非常复杂，具体最核心的老邓怎么下令军队开枪这个破事就交给历史学者们摆弄去吧<br />
当时来说，国内矛盾已经异常的复杂，这个在社会变革来说是很正常的。我们常说要改革，殊不知，任何社会改革都意味着会要损害一部分人的利益<br />
，从而令另一部分人受益，所以这不是随口说说就能干的事。<br />
<br />
当时来说，能引起如此大的波澜，无疑是有极其矛盾的社会背景、首先，保守一方如下岗工人之类因改革损害其利益（如下岗），某些干部因改革后诸多利益受损（如保守派干部）极其反对现有的国家政策。如同时作为单纯的学生以及其他的有识之士来说，政府改革的管理失控到处巨大的贪污腐败，当然祈求更进一步改革，达到我们常说的民主状态。两方人士互相交锋，导致这件事的爆发<br />
<br />
当然，个人认为，什么阴谋论之类也没什么，在政治上来说，本来谣言，污蔑，等阴谋都是达到利益目的的手段而已，至于当时是什么样的手段，要达到什么样的利益，觉得是搞政治那些人的破事了。<br />
<br />
而作为普通的一p民，我不带感情的说，看这件事的话主要看其对以后的影响。因为无论哪个政治行为都会有利弊的，关键在于其值不值<br />
<br />
六四后，国家到目前为止比较稳定，经济飞速发展，国力迅速提高，相信这是共同目睹的。<br />
然而，也因六四后，中国民主之路严重受挫。一直二十多年来，政府用经济成就来平抑民众对民主的渴求，同时严格限制与民主有关的行为。<br />
<br />
综合而言，个人认为，六四式利远大于弊的。也许在西方世界的人看来，死那么多人为了一些经济甚至丢弃民主的代价是很不可思议。、<br />
但我只能说。。。这是神奇的中国，不能用西方那种思维去考量这种事。<br />
至少来说，六四为中国奠定了通向民主的经济基础，如是早期的时代就想西方那样早步实现民主建制的话，我无法相信会发生什么骇人听闻的事。<br />
而，流血的代价来说。个人很同情，然而只能无奈，这国度就是这样，改变桌子的位置都要流一下血，我只能麻木看待了。<br />
试问，如果国家进入极其纷乱的状态，流血的会仅仅是那些学生吗？<br />
<br />
anyway，我很敬佩学生们的激情和勇气，尤其我也是一个学生看到现在的高校学风更是感慨。<br />
中国民主之路尚待更多理性，激情的人继续推进。<br />
祈求天佑我中华，勿令苍生太苦吧。<br />
<br />
纯粹有感而发，如有不当之处勿上纲上线，勿跨省。<br />
