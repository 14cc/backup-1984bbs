													<h2>你发现没有，一个有趣的现象</h2>
						
						
																											<div id="postmessage_668269" class="t_msgfont">你发现没有，一个有趣的现象：所有拒绝民主宪政化改革的种种理由、借口，其实恰恰是支持应该尽快进行民主宪政化变革的理由。<br />
<br />
比如说像什么中国人口太多、多数人素质低对民主缺乏认识、温饱生计更重要等，其实都是应该立即进行民主改革的理由才对。<br />
“人口太多”说明利益诉求更多元，那就更应该实行民主宪政制来使各方面的利益、诉求都得到维护和伸张，使各类少数者和弱势者均享有彰显自身权利的渠道。<br />
“素质低”是因为在专制下大多数民众没有对国家教育资源和经费的支配权，因此社会越专制则民众越愚昧、僵化、野蛮，这恰恰说明我们更应该尽早开启民主宪政化改革，让民众通过思想解放、言论和信息自由等来提高民主和文明认识，好比放在水中学习游泳，在民主实践中才能提高水平和素质，只有通过民主制度才能打破专制教育体制的腐败、僵化和不公平。<br />
而“温饱生计更重要”与民主化不仅不矛盾，相反只有民主宪政制度才能最有效的保障民众的“温饱生计”，而专制制度导致的贪污腐败横行、特权滥用、司法不公、挥霍浪费等，才严重威胁到民众的温饱生计问题。<br />
<br />
还有最常用的“西方、外国反华敌对势力借民主、人权问题瓦解、削弱中国”和“民主化导致社会动乱”等。<br />
实际上只有民主宪政化才能真正令中国崛起，专制下的腐败横行才会令社会堕落混乱，促进中国民主化并不会害了中国，反而会令我们健康的走向强大和文明，而如果不想让外国或西方来成为这一进程的主要引导力量，则中方更应该主动担负起促进民主化进程的责任，积极、主动开启民主宪政化改革，把实现民主化的主动权、和风头、功劳都“抢过来”。<br />
<br />
历史经验告诉我们，无论是历代的各种内乱暴力动荡，还是五千年未有的文化大革命空前浩劫，都是社会专制独裁体制下的产物，只有民主宪政制度，才能最有效地照顾到各方权益、制裁腐败和司法不公，才能最有效的激发社会活力和创造性发展，从而真正长久的维护社会稳定与和谐、良性的发展。要想在民主化进程中尽可能平稳、和平、顺畅的实现变革，则权力者们更应该尽早的、主动地开启这一进程，以实际行动来获得各方民众的支持和信任，从而实现共赢与良性互动的民主宪政化历史变革。<br />
<br />
凡此种种，综上所述，只要加以理性客观的论证分析，我们就会发现，那些各种拒绝走向民主宪政化改革的理由，不仅是站不住脚的，而且其实恰恰是应该尽快开启民主宪政改革的理由才对，所以每当它们又被当做“反对民主化”的理由而振振有词的提出来，我都不禁要问：你们为什么总在用支持民主宪政化的依据来证明反民主化的结论呢？<br />
拜托能不能讲点逻辑和理性，提高点素质和思维能力，不要总是自相矛盾下去。<br />
<br />
不知道大家以为如何？欢迎补充和讨论<br />
