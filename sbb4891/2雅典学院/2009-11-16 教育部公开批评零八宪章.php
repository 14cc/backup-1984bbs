													<h2>教育部公开批评零八宪章</h2>
						
						
																											<div id="postmessage_329782" class="t_msgfont"><a href="http://www.moe.edu.cn/edoas/website18/74/info1251957450312174.htm" target="_blank">http://www.moe.edu.cn/edoas/website18/74/info1251957450312174.htm</a><br />
【2009】 第3期<br />
<br />
理论中心秘书处编<br />
<br />
2009-09-03<br />
<br />
<br />
<br />
<br />
【编者按】 <br />
<br />
本期工作简报主要报道钟哲明教授关于“普世价值”问题报告会的重要内容。 关于“普世价值”，近年来、尤其在改革开放三十周年之际，理论界争论的越来越激烈，已成为当前人们关注的焦点。究竟该如何认识这个问题呢？<br />
<br />
2009年5月27日教育部关工委理论中心和北京交通大学关工委邀请到钟哲明教授来校做报告。钟教授本科毕业于北京大学，研究生毕业于人民大学，多年来从事马克思主义研究，曾担任过北京大学党委宣传部部长、马克思主义学院院长，北大学术委员会委员。现为中央马克思主义理论研究和建设工程专家，中国社会科学院马克思主义研究院特聘研究员，是一位资深的博士生导师。钟教授以深厚的马克思主义理论功底、以非常具体的事例，给大家做了一场深刻的报告。其中透彻的理论分析对开展关心下一代工作有很多启示，对如何构建学习型关工委提出很多问题和思路。 <br />
<br />
钟教授在报告会中讲了三个问题：一是“普世价值”争论产生的背景；二是如何看待普世价值；三是被人们鼓吹的普世价值中的具体内容，即所谓的民主、自由、平等、人权，乃至宗教，该如何看待？<br />
<br />
为深入开展理论研究，配合各级关工委探究如何把构建社会主义核心价值体系融入到关心下一代工作之中，现把钟哲明教授报告内容缩编刊载。<br />
<br />
 <br />
<br />
一、“普世价值”争论产生的背景<br />
<br />
普世价值作为学术、理论问题早已有之。但是最近三、四年来争论得很厉害，这有一个大的历史背景，就是中国的改革开放搞了三十一年。本有一个思路，就是先进行经济体制改革，随后搞政治体制改革。这些年来国内、国际有人预测，中国进一步的政治体制怎么改？按照什么方向、什么思路来改？各种各样的议论、各种各样的思潮。中国的改革开放一开头就面临着两个矛盾：一个矛盾是西方始终想扑灭中国社会主义之火，后来具体为西化、分化中国。再一个矛盾就是国内有少数人想照搬西方。小平同志很敏锐、很及时、很鲜明的提出反对资产阶级自由化，批判新自由主义。所谓自由化说到底就是把西方的那一套制度、观念搬到中国来，表现在搞经济体制改革时，他们有两个口号，一个叫市场化，一个叫私有化。一个市场化、一个私有化，明确地显示了他们搞经济体制改革的方向，也表明他们是将“市场”和“私有”当作普世价值观念向国人推销的。我们也要搞市场，但是我们要搞社会主义市场。我们要把过去单一的公有制改为公有制为主体，允许一点私有成分、包括外资成分存在，但是谁也没有说要全部私有化。<br />
<br />
经过多年的经济体制改革以后，那些人认为基本上得手了，提出要“攻坚”，加速政治体制改革。如何加速？他们早就心中有数，就是要把西方那一套政治观念、政治制度和盘地搬到中国来。具体地说就是要把自由、民主、平等、人权这一套西方的价值观念搬过来，就是要搞西方的“一二三多”：一，一个总统；二，两院平行；三，三权鼎立；多，多党竞选。然后加两“杆子”，抓笔杆子，就是新闻自由，抓枪杆子，就是军队国家化。显然，这是要推翻共产党的领导，改变社会主义制度。他们认为既然经济基础发生了这么大的变化，用他们的话说已经是“中国特色资本主义”了，那上层建筑也得变。他们把民主、自由这一套当作普世价值鼓吹，就是为达到 “一二三多”这个目标而制造舆论的。小平同志头脑很清醒，中国要解决贫困问题，要实现共同富裕，经济体制非改不可，市场取向也非搞不可，但是政治体制改革不能按照新自由主义那一套去搞。小平同志一开始就提出四项基本原则，指出四项基本原则与改革开放是党的基本路线的两个基本点。而政治体制改革要很慎重，要有分寸，始终批判那个“一二三多”，这些在《邓选》中可以查到。历届领导，江泽民也好，胡锦涛也好，政治体制改革的原则是很清楚的。现在有人鼓吹普世价值论，归根到底就是要动摇我们的四项基本原则。这次争论的实质就在于此。<br />
<br />
2003年，一些自称“自由主义者”的人提出“民主修宪”，认为宪法是非修改不可的，首先是删除包括四项基本原则在内的宪法序言。这几年来，有些人热衷于搞“宪政改革”，有人甚至引用毛主席的文章《新民主主义的宪政》，认为毛主席讲新民主主义宪政了，今天我们可以搞社会主义宪政。貌似有理，但他们忘记了有一个根本前提，当年毛主席讲的新民主主义宪政是针对蒋介石独裁的！蒋介石一党专政、一党独裁，根本不承认各抗日党派的平等合法地位，根本不承认中国共产党的代表以党派代表身份参加国民参政会。<br />
<br />
现在我们已经进入社会主义，有共产党的领导和共产党领导的多党合作与政治协商，实行社会主义民主，这一切都载入宪法。他们搞“宪政改革”是针对谁的？要改什么？为了什么？ <br />
<br />
2006年3月4日，所谓西山会议（北京），一些搞新自由主义的人明目张胆地提出“宪政”的目标就是多党制，希望共产党形成两派，希望军队国家化。大家看看新闻就知道现在台湾搞的政党模式多么混乱，党外有党，党内有派，动不动就游行、打斗。韩国、泰国都是多党制国家，现在的局面有目共睹。上台都靠金钱投入，下台后互相倾轧，贪腐坐牢的，跳崖自杀的，出国逃亡的，什么都有。<br />
<br />
军队国家化更清楚。不久前尼泊尔共产党（毛），在10年武装斗争，农村包围城市后，通过和平选举，执政了。但是一开头人们就发现一个问题，革命军队不被承认，原来的军队照样控制国家，早晚是颗定时炸弹。这次事件就是普拉昌德总理想调换控制旧军队的参谋长而引起的，结果自己辞职下台。可见军队国家化首先要看这个国家是什么阶级掌握的，军队归什么政党什么人控制，而不能将国家看成超阶级的、全民的，非政治的。革命政党不掌握枪杆子不行，胜利了，不打碎旧国家机器特别是军队、警察机器也是不行的。在这个枪杆子、刀把子问题上决不能有丝毫天真的想法，这是一个沉痛的教训。<br />
<br />
还有一个问题，如果实行所谓的“宪政”，那就是任何政党都要注册登记，通过普选才能执政，否则就没有合法性、合理性。有人在西山会议上就明确提出，共产党没有注册登记，是严重的违法，更不用说中宣部、团中央了，这些组织都是法律之外的机构。显然，这些人是拿西方的政治制度来硬套中国的现实。其实，当年美国、法国的资产阶级革命，革命中涌现出的政党、政派和形成的政治制度，曾向谁登记注册？又由谁批准合法？美国独立战争和南北战争更不用说了。什么叫革命，什么叫国家，什么叫法律、法制？离开历史条件和阶级分析，谁也说不清楚！<br />
<br />
面对这样的理论与实际问题，我们应当向唯物史观求教，要看锦涛同志的提法和中央的文件。锦涛同志提出三个“至上”：第一，党的事业至上；第二，人民利益至上；第三个才是宪法和法律至上。在我国，由工人阶级先锋队――共产党领导的社会主义、共产主义事业才是至上的，因为它代表了人口的绝大多数的共同利益、长远利益。党的事业至上，也就是人民利益至上。在这个基础上才是宪法和法律至上。唯物史观认为经济基础决定上层建筑的法律、宪法。只讲宪法法律至上，就陷入上层建筑决定经济基础的唯心史观。<br />
<br />
2007年，民主社会主义被炒得火热。《炎黄春秋》第2期发表了《民主社会主义模式与中国前途》一文，说民主社会主义是马克思主义的正统，只有民主社会主义能够救中国。从那时起，民主社会主义问题争论得很厉害。民主社会主义是西方资本主义发达国家内部实行的一种改良主义，社会矛盾激化后，用剥削全世界得来的钱搞一点福利，使老百姓的基本衣食有保障，不要起来造反，这一套可不是科学社会主义。民主社会主义已放弃了社会主义的制度目标，只把民主、自由、公平、互助等当作普世的伦理价值鼓吹，充当“资本主义病床边的医生”。我国的历史和现实证明，只有科学社会主义才能救中国，只有中国特色社会主义才能发展中国。<br />
<br />
2008年3.14西藏事件，西方说是“喇嘛革命”，实际是西方敌对势力通过达赖分裂集团精心策划的颜色革命。开头几天他们打砸抢烧，充分暴露，到了关键时刻，我们一出手，局势很快就控制下来了。这是中国的内政，内政不能干涉。他们把人权一类的普世价值搬出来也没用，因为比人权更高的是国权。今年我国的政府工作报告中，先是提出维持国家稳定，正式公布时加了一个国家安全。国家安全，必须时刻关注。<br />
<br />
2008年5月汶川地震，宣传普世价值的人又趁机大做文章，说这是普世价值在中国的实现。实际上在抗震救灾斗争中，“我们用理想凝聚力量，用信念铸就坚强，用真情凝结关爱”，同那些人讲的普世价值毫不沾边。这几年普世价值论不仅甚嚣尘上，而且什么东西都与普世价值上挂下联。以至于有些干部也抽象地宣传自由、民主、人道等等，自觉不自觉地离开社会主义核心价值，被鼓吹普世价值的人忽悠。<br />
<br />
2008年10月，中央三中全会上，胡锦涛同志两次讲话，使我们提<br />
<br />
高了认识：经济工作搞不好，会出大问题；意识形态工作搞不好，也会出大问题。现在意识形态领域存在渗透与反渗透的斗争，敌对势力对我搞渗透演变，旗号是自由、平等、人权、宗教等等，目标对准中国共产党的领导和社会主义制度。国内有噪音、杂音，否定党和社会主义，否定改革开放，我们不能掉以轻心。意识形态工作全党有责，要看好阵地、管好队伍，领导权牢牢掌握在马克思主义者手中，确保意识形态安全。<br />
<br />
问题的本质是社会主义价值体系与资本主义价值体系的较量。老祖宗马恩主要讲两种世界观的斗争，提出无产阶级世界观和资产阶级世界观的斗争。列宁讲两大思想体系的斗争，要么是资产阶级的思想体系，要么是无产阶级的思想体系，没有第三种。毛泽东时期讲两种世界观的斗争，人生观讲得很少。改革开放后，价值观流行起来，受西方的影响越来越大，潮水般地涌进来，堵也堵不住，怎么办？只有引导，中央提出建设社会主义核心价值体系。指导思想是马克思主义，而不是西方这个主义那个主义的资产阶级思想。共同理想是中国特色社会主义，而不是西方这个模式那个模式的资本主义。精神支柱是以爱国主义为核心的民族精神和以改革创新为核心的时代精神。道德风尚，用社会主义荣辱观来引领。这些都同西方大不一样。两种价值体系的较量与斗争，说到底是两种意识形态、思想体系和世界观的较量与斗争。<br />
