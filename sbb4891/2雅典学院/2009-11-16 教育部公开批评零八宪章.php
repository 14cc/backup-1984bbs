2008年还有一件不能掉以轻心的事情就是“08宪章”的出台，12月10号公布，挑选的日子是联合国世界人权日。1977年，捷克斯洛伐克一些反政府人士以争取人权的名义形成“宪章运动”，发表宣言，即“七七（年）宪章“，先后签名者一千多人，1979年被禁止公开活动。1989年11月后，反对派以“七七宪章”为首，联合成立了“公民论坛”，直到1990年6月组织政府，捷共交出政权。“08宪章”就是仿效这个“七七宪章”。“七七宪章”把捷克斯洛伐克搞垮了并一分为二，现在有人也想把社会主义中国搞乱、搞散、搞垮。“08宪章”是什么内容？它的基本理念是自由、人权、平等、共和、民主、宪政。基本主张是修改宪法，要把那些普世价值弄到宪法里面去。再就是搞分权制衡，司法独立，撤销党的政法委，实现军队国家化，共产党退出军队，公务员保持政治中立，设立人权委员会，保障人权，开放党禁，政党活动自由，搞多党制等等。还要把刑法里面的“煽动颠覆国家政权罪”条款废除。除此，还提出确立和保护私有财产权利，推进土地私有化，建立中华联邦共和国等。说到底，“08宪章”把这些年来他们鼓吹的西方普世价值，政治制度和私有制，通通集中起来成为对抗共产党和社会主义的政治宣言和行动纲领。“08宪章”是海外反华敌对势力和国内资产阶级自由化分子，甚至体制内某些人相互勾结的产物。“08宪章”出笼后的12月下旬，胡锦涛同志在军队一次重要的会议上提出当代革命军人核心价值观是：“忠诚于党，热爱人民、报效祖国、献身使命、崇尚荣誉”。强调忠诚于党就是要自觉坚持党对军队的绝对领导，任何时候任何情况下都要坚决听党指挥。<br />
