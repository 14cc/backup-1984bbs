二、&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;普世价值问题为什么越炒越热？&nbsp; &nbsp;<br />
<br />
普世价值是什么？至今没有任何权威的书籍和词典，明确地对普世价值下过定义。世界一切事物都是随时间、地点、条件而变化的，哪有绝对的，永恒的。联合国有一个委员会，集各国专家1997年开了一个会，想找出普世的伦理价值，普世的伦理观念，结果各种文明、各种宗教背景的人都不同意，说找不出来，无果而终。<br />
<br />
普世价值问题究竟是怎么炒起来的呢？提出以下三个方面思考。<br />
<br />
一是社会思想来源。人类进入阶级社会后，所有的统治阶级，为使私有制万世长存，都把自己的阶级思想变成全社会的统治思想，说它适用于所有的人，所有的时代。特别是他们的思想家，政治家，法学家，从现实许多不同的事物中抽出一般性、共同性，然后使它独立化，并无限扩大，成为绝对的永恒的。这就是最早形成普世价值观念的思维方式。<br />
<br />
二是宗教神学来源。三大宗教，都把自己信仰的对象说成是至高无上，全知全能，爱所有的人，普度众生，永无谬误，永远存在的神。世上本无什么神，但经过宗教神学的论证和加工，以及多种历史和社会的原因，许多人都相信神。也有人说儒家就是儒教，孔夫子的字字句句只能相信不能怀疑。这是封建礼教那套东西。例如《圣经》里的“切勿偷盗”，其他宗教也说不要偷，不要抢。恩格斯专门分析过这个问题，原始社会和未来的共产主义社会，都没有私有财产，根本不存在偷盗问题。只有私有制社会，财产分你的、我的，才出现了偷盗。不同的阶级对偷盗有不同的看法。比如对于农民来说，地主阶级、封建统治者靠横征暴敛而致富。把他们的发不义之财拿过来，理直气壮。对于无产阶级来说，资产阶级剥削了剩余劳动，拿过来归社会所有，理所应当。有一位空想共产主义者说“财产就是盗窃”。这句话很有名。资产阶级的财产怎么来的？剥削来的，偷来的，抢来的。把剥夺者的财产还给工人，农民，叫物归原主，名正言顺。资产阶级反对偷盗，是为了保护他的私有制，掩饰自己的巧取豪夺，欺世盗名，抢权窃国。社会主义初级阶段，不许偷不许抢，是为了保卫社会主义和广大人民的合法财产。<br />
<br />
三是西方、特别是美国的鼓吹，这是普世价值问题被炒热的决定性原因。多年来美国不遗余力地推行霸权主义。二战刚结束，美国政要就提出要瓦解苏联，首先是“把脑子弄乱”，“改变人民的价值观念”，“把主要的赌注压在青年身上”。苏东剧变前夕，美国学者提出“历史终结论”，认为西方的自由民主制度已取得最终胜利，美国模式是“人类普世价值和最终统治形式”。1991年，美国总统布什宣称“进入了一个新的时代”，“我们可以按照自己的价值观念和理想建立一种新的国际体系了”。然而两极体制结束后，资本主义的基本矛盾和世界的主要矛盾依旧存在，一些更深刻的矛盾刚刚显露出来。全球冲突四起，小战不断，比过去还厉害，至今咱们周边几个国家还在打。怎么解释？美国学者来了一个文明冲突论，把它归结为基督教文明、伊斯兰教文明、佛教文明的冲突，以此掩饰不同经济不同政治制度相互对立这个根本原因。美国政要和学者把西方制度、理念乃至基督教义当作普世价值，以各种手段向别国输出，大肆推行人权外交，打人权牌。1999年以美国为首的北约轰炸南联盟，并袭击我驻南使馆，制造了一场“人道主义灾难”。又在全球意识，全球化旗号下，宣扬民族国家已经“过时”，向非洲某些国家推销美国式民主自由，使得政局动荡，纷争不休。对伊拉克用战争输出民主自由制度，连年劫难，使得伊拉克成了“战争地狱”。对独联体某些国家，通过其代理人搞“街头政治”，实行“颜色革命”，使之进一步西化和倒向美国。所有这些就形成了如此态势：当今唯一的超级大国对中国这个最大的发展中国家和最大的社会主义国家的包围圈越拉越近，越缩越紧了。新加坡有学者说，“如果看一下地图，中国是世界上唯一一个被核武器所包围的国家”。<br />
<br />
当今，“全球化”一词经常用，但是我们不要抽象地宣传全球化。抽象的文明、抽象的现代化、抽象的市场、抽象的全球化，这些抽象的东西都是不存在的。有些好心的糊涂同志说，中国文明难道不是普世文明吗？马克思主义普遍真理难道不是普世真理吗？这样说也不科学。<br />
