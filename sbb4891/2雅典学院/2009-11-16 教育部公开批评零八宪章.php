 <br />
<br />
三、如何看待民主、自由、平等、人权<br />
<br />
这是政治色彩比较突出的问题。<br />
<br />
民主讲得是最多的。有人认为政治体制改革的目标，全世界都是民主。有学者说，“民主是个好东西”。问题在于民主不能简单地用“好”或“不好”来判断，需要做具体的历史分析。因为世上没有抽象的民主。民主是和国家政治连在一起的。民主指一种国体，即国家的阶级性质；也指一种政体，即政权的组织形式；也指一种权利、作风和方法，如民主权利、民主作风、民主方法等。“民主”来源于古希腊，原意是“人民的权力”。但不同时代、不同国家的“人民”，包括着不同的阶级、阶层和社会集团。最早的民主是古希腊政治的一种议事方式，发展到今天就是少数服从多数。一人说了算，或绝对民主人人同意才算，两个极端都不好。前者无民主可言，后者看似民主，实际上是一票否决制。我们的国体是人民民主专政；我们的政体是人民代表大会制度即民主集中制；我们的政党制度是共产党领导的多党合作和政治协商制度。协商也是一种民主形式等等。将来人类最后消灭了阶级，作为阶级统治工具的国家也自行消亡，同国家政治相联系的民主，也就随之消失了。那个时候没有现在的这种民主，但有自由。共产主义社会是自由人联合体。<br />
<br />
自由，源出拉丁文，最早的含义是从束缚中解放出来。人类受自然规律的束缚，在阶级社会受阶级和国家的束缚，参加政党受政党的束缚等。不同时代不同的人们都想：如果能摆脱各种束缚，该多好！于是人们就把自由这个抽象观念传承下来，并注入自己所需要的内涵。在奴隶社会，奴隶是没有人身自由的。古罗马奴隶起义要争的“自由”，就是人身自由。资产阶级向谁争自由？首先是贸易自由，资本如果不能自由流动，劳动力如果不能自由买卖，资产阶级怎么剥削雇佣工人而赚钱？同贸易自由相联系，资产阶级要求“个人自由”、“个性解放”，以便从封建的政治和思想束缚下解脱出来，获得全面发展资本主义的自由。资产阶级成为统治阶级后，不能不在政治上宪法上赋予人们这样那样的一些自由权利。但它有个底线，什么时候触及资本主义所有制，威胁到资产阶级国家的安全，就立即取缔，把“自由、平等、博爱”代之以毫不含糊的“步兵、骑兵、炮兵！”可见在阶级对立社会里，自由不过是剥削阶级剥削广大劳动人民的自由。只有生产资料公有制和无产阶级领导的社会主义国家，才能使部分人所享受的自由，变为绝大多数人能真正拥有的自由。有人说自由选举，一人一票，这才是真正的自由！不要忘记，西方今天的自由票选，是经过100多年甚至200多年慢慢才有的；美国黑奴问题经过南北战争才解决；西方许多发达国家的妇女，到上世纪50，60年代才有选举权。除了种族歧视，性别歧视，还有财产之类的歧视。亚洲某些国家，如印度、日本、韩国等，看起来是自由选举，但选来选去，当总统、总理、首相的大都出自世家、豪门、望族。西方的一人一票，固然来之不易。但是你那一票不过是沧海一粟，起不了多大作用，最后还是谁竞选费用高，谁当选。奥巴马当选是因为他破选举经费记录，筹得了历史上从来没有过的最高的竞选经费，这是他获胜的奥秘所在。当选后你看，两党制马上变成一党制了。谁的党？总统的党，民主党，共和党，都联合在总统周围。是不是中国永远不搞自由选举？不！小平同志对外宾讲过，2050年左右，各种条件具备后可以搞。现在西方有些反华的人也承认，在中国共产党领导下，中国实现了历朝历代都做不到的富强。什么叫执政的合理性合法性？这就是！对此，我们是既有决心也有信心的。<br />
<br />
平等，原意指人们追求同等的社会地位和权利。佛教讲“众生平等”。公元前一世纪小亚细亚的奴隶起义，提出没有穷人和富人、奴隶和主人的平等理想。中国的农民起义，主张“均贫富，等贵贱”。资产阶级反封建时要求“等价交换”；《人权宣言》中提出“在法律面前，人人平等”。所有这些，都起过历史的进步作用。但在阶级对立社会中，根本不可能实现真正的平等。无产阶级接过平等的口号，赋予新的解释。平等，最根本的是消灭剥削，消灭阶级。这就抓住了问题的本质。但在消灭剥削，消灭阶级的共产主义社会的第一阶段，由于各人劳动能力不一样，家庭人口不一样，劳动者的收入水平和实际生活水平就不一样。这种形式上的平等和事实上的不平等的弊病，只有到共产主义高级阶段，实行“各尽所能，按需分配”时才能消除。恩格斯认为，即使那时，不同地方之间总会有生活条件方面的某种不平等存在，如阿尔卑斯山的居民和平原上的居民的生活条件总是不同的。又如按需分配，老人和年轻人的需要也很不一样。这些说明平等的观念同民主的观念、自由的观念一样，总是具体的、历史的产物。马克思主义认为，平等的实质是消灭阶级与阶级差别，超出这个范围的平等，必然流于荒谬，走向平均主义。<br />
<br />
人权、人道，小平同志做了很多解释。小平同志认为在社会主义社会里也不能抽象的讲人、人的价值和人道主义，否则，就会把青年引入歧途。他讲人权的时候，首先问是少数人的人权，还是多数人的人权、全国人民的人权？小平同志说我们支持人权，但更重要的是国权。中华人民共和国成立之前，中国人站立起来之前，根本没有国权，谈何人权？那个时候对外首先是争国权。<br />
<br />
民主、自由、平等、人权，已经流传千百年，无产阶级掌握政权后，绝不是不讲自由、民主、平等、人权。我们既要讲，又要赋予新意，赋予马克思主义的解释。这些年来我们讲向西方学习，应该全面认识，既看到同中有异，又看到异中有同，具体分析它的同与异。不因同而肯定一切，照抄照搬；也不因异而否定一切，拒绝交流吸收。只有这样才能驳倒和摈弃普世价值论</div>

							
							
							
							
