													<h2>庞氏骗局终于撑不住了</h2>
						
						
																											<div id="postmessage_441919" class="t_msgfont">庞氏骗局，传销的“老祖宗“，查尔斯・庞齐”发明“的，以高回报率为许诺，骗取投资，用未来人的投资去偿付前期的投资者――都是这种把戏。<br />
　　　　<br />
　　　　 说白了，就是告诉你交一千块钱，一年之后可以分到五千块，这些钱哪里来的？就是一年间其他跳进这个坑的人的钱，只要有人源源不断的往里跳，就可以一直骗下去，但是……但是需要”拉拢“的人是几何增长的，总有一天会入不敷出，这时候”先驱者“卷款而逃是必然的，血本无归之正是那些揣着发财暴富梦的人。<br />
　　　　<br />
　　　　 明白了庞氏骗局，再看社保就要倒吸一口冷汗了。社保的本质是什么？就是用现在人的交的钱养以前的人，我们的钱养了他们，那我们这批人老了怎么办？理论上是50年后交社保金的那些子子孙孙们的钱养我们的老。<br />
　　　　<br />
　　　　 典型的传销！所以骨子里是一样的，需要有人前仆后继的往里面跳，才能艰难的维持收支平衡，就算是最差的，毫无回报的――交了多少退休后能领多少，一分利息没有，还完全不给你算购买力的贬值，这种情况下也得是跳进去的人不能少，不然社保铁定赔钱，直至破产。<br />
　　　　<br />
　　　　 问题是，我们现在还有多少人再跳？以后还能有多少人跳？<br />
　　　　<br />
　　　　 Socialism的巨大优越性给我们指引了一系列政策：<br />
　　　　<br />
　　　　 首先要赞扬基本国策――计划生育。老龄化是世界难题，但是中国这个国策造就了世界最独特的未富先老，面对4-2-1家庭巨大的养老压力，“专家”说可以社会养老，言下之意便是社保，难道老年人做在养老院就能长出财富？消耗的财富不还是年轻人去创造。七八十年代出生的我们这波人还能创造财富，还能通过社保的方式去养现在的老年人。50年后，我们老的时候，中国还能有多少年轻人创造财富？<br />
　　　　<br />
　　　　 再就是，中国的社保体系处于相当初级的阶段，就在几天之前（1月1号可以转了，后面重点扯一扯这个看上去似乎很美的政策）转社保还是一个不可能完成的任务，现在社会人口流动性如此巨大，大部分人不可能一辈子待在一个城市，尤其是农民工跟大学生，不能转意味着一旦你离开这个城市，除了能取出你自己交的小部分，大部分钱就当是支援当地发展了……而且现在已经不能取了。所以很大一笔社保钱会留在当地社保账户里面，官老爷是不能看着这笔钱寂寞下去的……<br />
　　　　<br />
　　　　 还有，中国现在吃社保的这批人，都是计划体制延续下的人，那时还没有现代社保的概念，他们以从没有往这个社保体系输过血，属于纯倒贴。<br />
　　　　<br />
　　　　 长期高度的通货膨胀让社保发的几个钱成了笑话，只能一再提高标准，亏空越来越严重。只记得小时候我妈告诉我拿五毛钱去集市买的菜一个人拿不了，现在五毛钱，哼哼，我只知道今天北京大白菜一块五每斤，没有几块钱一颗白菜是拿不走的。<br />
　　　　<br />
　　　　 还有更是中国特色了，社保金无人监管，社保挪用几个亿的报道已是家常便饭。前几年郎咸平在上海做了一个财经节目，里面说了上海某个官员的问题，节目马上就被封了。不久后就传出，那位官大爷被爽龟，挪用社保N亿的煌煌战绩。<br />
　　　　<br />
　　　　 2010年1月1日，一个值得纪念的日子，中国广大农民工跟白领打工仔又被吃果果代表了一把，又被活活退了一层皮――社保不让退了，但是可以转了，最高转12%，如果你觉得无所谓走到哪里转到哪里就是了，你就悲剧的无以复加了――你在北京交了十年了，想转回家乡县城）在当地领，是需要当地倒贴钱给你的，当地的领导能同意接收？你能转成么？<br />
　　　　<br />
　　　　 社保撑不住了，亏空太大了，没有更多的人往里跳，只能卡住跳进去的人的脖子不让你出来。年底，中国人都太需要钱了，年关，年关，过年就是过关。所以每年年底，都是退保的集中时期，官老爷看不下去了，必须把这些刁民卡死了，不然哪来那么多钱给他们退。<br />
　　　　<br />
　　　　 于是，在我们敬爱的毛主席逝世30多年后，雷厉风行的行政作风终于回归了这片神奇的国度。这片热土上，已经很多年没有看到哪个法律条文执行的如此彻底迅速，从中央下文件到省市开电视电话会议部署，到各地执行，不到一周。<br />
