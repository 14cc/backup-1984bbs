　　　　<br />
　　　　 联想到差点也是雷厉风行的电动车征税，总结出一个定律――两个凡是：凡是能快速准确保证执行的政策都是政府需要从刁民身上刮钱的政策；凡是拖拖拉拉，N年调研N年讨论N年立法N年后实施，并且实施后基本等于没有的政策都是要摆摆架势保护下老百姓的。比如鼓捣了N年保护私有财产的《物权法》，落到最后，还不如个《拆迁条例》好使。<br />
