													<h2>建筑工程抗震设防分类标准</h2>
						
						
																											<div id="postmessage_570468" class="t_msgfont">1.0.2 本标准适用于抗震设防烈度为6～9度地区房屋建筑工程和市政基础设施工程的抗震设防分类。<br />
============<br />
3.0.2 建筑应根据其使用功能的重要性分为甲类、乙类、丙类、丁类四个抗震设防类别。<br />
甲类建筑应属于重大建筑工程和地震时可能发生严重次生灾害的建筑，乙类建筑应属于地震时使用功能不能中断或需尽快恢复的建筑，丙类建筑应属于除甲、乙、丁类以外的一般建筑，丁类建筑应属子抗震次要建筑。<br />
<br />
3.0.3 各抗震设防类别建筑的抗震设防标准，应符合下列要求：<br />
1 甲类建筑，地震作用应高于本地区抗震设防烈度的要求，其值应按批准的地震安全性评价结果确定；抗震措施，当抗震设防烈度为6～8度时，应符合本地区抗震设防烈度提高一度的要求，当为9度时，应符合比9度抗震设防更高的要求。<br />
2 乙类建筑，地震作用应符合本地区抗震设防烈度的要求；抗震措施，一般情况下，当抗震设防烈度为6～8度时，应符合本地区抗震设防烈度提高一度的要求，当为9度时，应符合比9度抗震设防更高的要求；地基基础的抗震措施，应符合有关规定。<br />
对较小的乙类建筑，当其结构改用抗震性能较好的结构类型时，应允许仍按本地区抗震设防烈度的要求采取抗震措施。<br />
3 丙类建筑，地震作用和抗震措施均应符合本地区抗震设防烈度的要求。<br />
4 丁类建筑，一般情况下，地震作用仍应符合本地区抗震设防烈度的要求；抗震措施应允许比本地区抗震设防烈度的要求适当降低，但抗震设防烈度为6度时不应降低。<br />
<br />
3.0.4 本标准仅列出主要行业的甲、乙、丁类建筑和少数丙类建筑的示例；使用功能、规模类似的建筑，可按相近的建筑示例划分其抗震设防类别。本标准未列出的建筑宜划为丙类建筑。<br />
