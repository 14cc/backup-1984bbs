													<h2>我对同人于野爱国常识论的理解</h2>
						
						
																											<div id="postmessage_486969" class="t_msgfont">一个基本认识，直接否定爱国的价值是愚蠢的。冉云飞就很聪明，他从来不说不该爱国，他只说爱国不应该以现在政府煽动的这种方式。我曾经的说法则是，连这个国家的人民你都不爱，还怎么配爱国？<br />
<br />
现在同人于野考量的不是爱国的定义，而是普通民众这种看似盲目的爱国情绪的发源基础。他说的很简单，爱国是一种自然的主观需求，就像人要喝水一样，既然有需求就会产生供给动力，有人要喝水，就会有人卖水，有普遍的爱国情绪的需要，就会有人提供爱国产品满足人们的这一心理需求。他已经说的很清楚了，这种需求有不自觉的功利导向，一个人爱国的爱国需求程度并不总和这个国家对他如何直接成比例相关。<br />
<br />
我认为同样解释中国大多数民众在爱国上的奇特行为，他的这个解释要比民主食油党们所谓'愚民宣传'甚至’老百姓天生傻逼愚蠢‘（因此需要我们精英挺身而出替他们思考告诉他们如何才是爱国）这一类的陈词滥调要靠谱的多。<br />
<br />
一味枉顾现实，只知道掩耳盗铃的让对现实的解释与描述机械的符合自己的固有意识形态，这就是教条主义，这就是政治行为宗教化，面对土共这样拥有近乎无限资源的对手时，结果只能是碰的头破血流。<br />
<br />
当然，这里的人除了关二少数几个人外，没有多少人对政治实践真正感兴趣，只求发泄现实中积累的对土共和现实的不满，所以这些话对他们没价值，说了也白说 -- 人家来这儿本来就是找五毛开骂爽嘴爽心来的，对民众的爱国心态的解释到底是不是符合现实，他们才懒得关心呢。但对于关二这种真有心煽动老百姓造反推翻共产党的人来说，如果他不是发神经，那早晚有一天他要严肃认真的对待这个问题 -- 这不再是在bbs上空口白牙的表表态，而是自己的政治前途的问题。<br />
<br />
秀才造反，十年不成，就是因为秀才往往分不清意识形态的正确和认识现实的正确之间，并不总是一致。山大王总是修改理论，让理论符合现实；秀才总是修改现实（的描述），以便让现实符合理论，因为秀才太骄傲，不能承受修改现有理论（等于承认自己意识形态上的失败）带来的屈辱和痛苦 -- 所以最后秀才造反，大多以殉道告终 -- 所谓殉道，就是明知道所谓的”道“不符合实际走不通的时候，仍然不肯修改理论而是傻不鸡鸡的去撞南墙，最后做了烈士顺便拉一大帮人陪葬还觉得自己很光荣。<br />
<br />
