																											<div id="postmessage_487004" class="t_msgfont"><strong>论爱国的理由</strong><br />
<br />
　　网上的人士形形色色，左派、右派、极左派、极右派、反美的、反日的、媚美的、哈日的……。林林总总、千奇百怪、叽叽喳喳、争来吵去，宛如林子里的傻鸟们。但是一提到爱国，却能从不同的嘴里发出共同的声音。大家都是爱国的。呵呵，有趣，非常有趣。<br />
<br />
　　人们常说，世界上没有无缘无故的爱。那么“爱国”的这种爱，这种非爱不可的爱、这种不爱有罪的爱，他的根源在哪里呢？试剖析之。<br />
<br />
<strong>理由一：爱国是爱国土</strong><br />
<br />
　　国家首先是国土，虽然巴勒斯坦的国父阿拉法特号称把国土顶在头顶上，但是他的继承者们直到现在还在为建国奋斗着。所以，我们的分析从国土开始。<br />
<br />
　　我们从小生活在这片土地上，这片土地养育了我们。所以我们熟悉它、热爱它。这很合情合理。我就无比热爱我的家乡沈阳。你骂我可以，谁不客观的评论我家乡的人和物，我肯定心里不舒服。<br />
<br />
　　但是，因为这个原因爱家乡我可以理解，那爱整个中国我就理解不了了。远的不说，就说赵本山大叔所说的“大城市”铁岭，就在沈阳边上，我去都没去过，我凭什么爱它？<br />
<br />
　　有人说，虽然你没在那个地方生活过，但是那个地方的物产也养育过你呀！但是我要说，美国的杏仁，日本的寿司也养育过我，为什么我非得爱全中国呢？<br />
<br />
　　显然，这个理由是不充分的。即使你走遍了全中国，那也仅仅是你找到了爱国的理由，没有道理要求每个中国人都非爱不可。<br />
<br />
<strong>理由二：爱国是爱民族</strong><br />
<br />
　　世界上大多数国家都是民族国家。即使是像美国那样的移民国家，也会虚构出个美利坚民族出来。而我们中国，则有中华民族的说法。<br />
<br />
　　民族是国家的实质内容之一，所以爱民族也是爱国家的一种理由。<br />
<br />
　　我爱我生活中忙忙碌碌的众生，我也爱我生活之外勤劳朴实的众生。无论在乡间，还是在城市。他们为生存而奋斗的过程就是一种惊心动魄的美。我爱沈阳铁西区的下岗职工；也爱千里之外劳作着的贫苦农民；也爱在灯红酒绿中用自己的劳动所得尽情享受生活的市民；也爱网线另一端为自由而思考着，奋斗着的知识分子们。<br />
<br />
　　老百姓不就是这样吗？平凡而又可爱。哪一个国家的老百姓不是这样呢？美国的？日本的？都是一样的可爱。为什么只爱本民族？难道其他民族就头角峥嵘？不会的。基督教徒的普世之爱，马克思主义者的阶级之爱，都要比在爱国主义束缚下的民族之爱，更加宽广、自然、合理。<br />
<br />
　　当然，有人还会提出血缘。难道你不觉得你的一些亲戚，要比跟自己没有什么亲属关系的朋友更加疏远甚至面目可憎吗？那就不需要继续说什么了。<br />
<br />
　　所以，爱民族也不能构成必须爱国的理由。<br />
<br />
<strong>理由三：爱国是爱我们国家历史形成的文化传统</strong><br />
<br />
　　在历史的长河中，由于地缘因素的影响，这个世界长时间内被分裂成若干个文化圈。如欧洲的基督教文化圈，阿拉伯的伊斯兰文化圈，东亚的儒文化圈等等等。在文化圈内的各民族，有着相近的文化思想和意识形态。而每个国家也有着占主体地位的文化。“我爱中国这片土地上孕育出的文明，所以，我爱中国。”这也是可以理解的，接受的。但是，这也并不能说明为什么“国人就必须爱国”这个“大道理”。<br />
<br />
　　因为，每个人的思想是不一样的，他也许认为美国的、日本的、或者北朝鲜的文化看起来更好，更适合他，于是他爱美国、日本、或者北朝鲜甚于爱中国。虽然他还是中国人。难道这就有罪了吗？比如一个铁岭人，去了趟北京，回来就爱上北京了，总想从铁岭这穷地方跑出去，到北京去。难道这个铁岭人就犯了“叛乡罪”了吗？当然不能这么说。<br />
<br />
　　而且，一个人并不一定只爱一种文化，他可以既喜欢中国文化，也喜欢日本文化。也就不一定只爱一个国，可以既做中国的爱国者，也做日本的爱（日本）国者。并不冲突。<br />
<br />
　　其实，大多数横眉立目的爱国者的爱国并不是由于上述三个因素。而是因为下面的理由。<br />
<br />
<strong>理由四：爱这个国家的政治制度及政府</strong><br />
<br />
　　如果在民主国家，这种爱国是可以理解的。比如欧洲人爱多党制，美国人爱两党制。一个人不可能既爱法国的多党制，又爱美国的两党制。既爱君宪制，又爱共和制。当然，更多的分歧并没有接触到这么严重的概念，仅仅是一些制度养成的习惯上的不兼容，因此他们会互相攻伐，横眉立目，党同伐异，压制少数这样的爱国互相排斥是可以理解的。因为他们的政治制度是得到国内大多数人拥护才建立起来的。<br />
<br />
　　而作为一个现代人，对于中国的这种爱国者就不可理解了。难道让我们爱报刊审查制度？爱言论管制？爱收容遣送（现在叫帮助制度）？爱文字狱？这帮人脑袋有病吧，除非他们都是既得利益者。<br />
<br />
　　我们中国人就是再愚昧，基因是没有问题的，不会傻到那个地步。大家脑袋都没有问题。你看看那些贫穷的中国“爱国者”他们叫嚣反对着的都是比中国富裕得多的国家。如美国日本。谁爱他们，为它们说两句好话就会被扣上“汉奸”的帽子。你要是爱跟中国生活水平差不多的国家，如俄罗斯、印度，就没这么大危险了，你要是爱远不如中国的国家如哈萨克斯坦、吉尔吉斯坦那就更是毫无危险了。尽管他们也跟中国有领土争端，甚至面积是钓鱼岛的N倍。印度造再大的航母，甚至明确针对中国，他们也不会像反日一样反对它。我们中国的“爱国”者只会对富裕的国家使劲叫嚣。因为驱动他们的“义愤”的不是对国家的爱，而是对比自己富裕的国家的嫉妒。这帮人，像傻老娘们一样。嫉妒让他们红了眼睛，充当了卑劣的政治家的工具。<br />
<br />
　　爱国有很多理由，不爱国也有很多理由。爱国的人有爱国的自由，只要他们不对和平造成威胁。不爱国的人也有不爱国的理由，除非他们泄露国家机密。而大多数老百姓是不会接触到真正的国家机密的。<br />
<br />
　　爱国也有很多方式，不能说反日（美）就是爱国，否则就是叛国。爱国者们不应该像宗教裁判所的神甫那样，拿着自己的量天尺，把别人都说成是异端。爱国是一种情感，情感是不能用教条和法律硬性规定的。爱国不是义务，爱国也不是卑劣行径的遮羞布。那些把爱国“宗教化”甚至“邪教化”的爱国者，如果不是盲从的愚蠢教徒，就一定是别有用心的野心家。<br />
<br />
　　人们啊！我是爱你们的，你们要警惕啊！<br />
