																											<div id="postmessage_451543" class="t_msgfont">“你死在这里有谁知道？当局也不会知道，许志永也不会知道。”来劝我的小官员，给我浇冷水。<br />
<br />
我笑：“当局不知道，那正是你们的卑鄙和失职之处。你们没起到上情下达、下情上达的作用。你们把我关在这荒山野岭的地方，与世隔绝、信息隔绝，我所有的艰苦和英勇外界都‘不知道’，这正是你们的卑鄙。至于许志永，他会知道的，他不会死在牢里的，等他出来了，他会知道的。”<br />
<br />
一个小官儿被我说窘了，换一个来讹诈我：“你上次不是写了保证不闹事么？为什么不讲信用？为什么到了北京要闹事？”<br />
<br />
我愤怒地顶回去：“你可听清楚了！我上次写的不是‘保证’！油田企业没资格要我劳务之外的‘保证’！我写的是‘说明’！说明自己去北京不会从事‘非法活动’，我们民主主义者的合法非法，判断标准是联合国人权公约！而且，上次被你们欺负胁迫着写了个‘说明’我都后悔了，这一次，你们一个字也别想拿到！悔过书、保证书，一个字也别想拿到！――我对同志们的要求、和对我自己的要求是不一样的！对我的同志们，我是按英美国家战俘的标准来要求的。战俘是没有所谓背叛的，战俘在胁迫之下所发表的言论，不受追究。所以晓波写悔过书，我可以原谅。张怀阳要被抓，我告诉他可以服软――而我对自己，是按江姐和许云峰的标准来要求的！所以，你们一个字也别想拿到！”<br />
<br />
江姐和许云峰，这两个锐利的名字，一下打杀了对方的气焰，让他气虚而去――<br />
<br />
可是，可是……<br />
<br />
…………<br />
<br />
又换了一个来浇冷水：“你就是死了，许志永也照样在监狱里出不来！”<br />
<br />
我热血上涌：“那好啊，那就是他当英雄，我当烈士，两个人各写各的传奇！能在自己还算年轻的时候，遇到一个值得你为他去死的人，遇到一件值得你为它去死的事情，那是多么幸福！当烈士是多么幸福！（笑）我正在幸福呢，别打扰我！”<br />
<br />
“烈士？……谁承认？”<br />
<br />
“我的宣言已经发出，我的热血文章已经传世，现在我要用生命来增加悲剧的重量，让我的后代读者们，流着泪读我的文章！想想啊，能让代代读者流着泪读我的文章，那对一个写作者是多么大的诱惑！――”<br />
<br />
壮丽河山上的千万点眼泪，把虚荣的、小资产阶级革命者刘沙，诱惑得热血沸腾！<br />
<br />
几个官员面面相觑，感叹着：“你瞧，你瞧她的眼睛都在闪亮――”<br />
<br />
我心里冷笑，这些瞒顸落伍的官员，你跟他说公民权利他根本不懂、他感动不了！只有用英雄豪情来反击才能让他们触动，让他们明白一点！――共产党自己就是靠制造英雄、烈士起家的，它太知道一个烈士，一个女烈士的感染力和杀伤力了！<br />
<br />
可是，可是…………<br />
<br />
…………<br />
九月初那惊心动魄的残酷经历，让我看到了对方狂暴的兽性，让我看到了“GCD的凶残超乎我的想象！”“社会主义来到世间，从头到脚，每个毛孔都滴着血和肮脏的东西！”“建国六十年来，建党近九十年来，光明的表面下尽是罪恶、血腥，为了一点微不足道的利益，他就可以拿无辜者的生命来垫！”此前我对GCD最多只是愤怒，愤怒时还都要坦诚，还要告诉他我愤怒了！而此后，我学会了仇恨，学会了掩饰仇恨，我仍然只说真话，但不再说全部真话！<br />
<br />
可是，可是…………<br />
<br />
九月中旬，外表平静的我，在早秋的金色阳光里看书：托克维尔《论美国的民主》――<br />
<br />
“凡被斗争的热情所激发，在反对反对者的观点和暴行时使自己的观点超过其自然界限的人，都忘记了自己追求的目标。发表了不太符合自己的真实感情和笃厚天性的言论。”<br />
<br />
“在欧洲，我们很难判断民主的真理性和不变性，因为欧洲有两个互相对立的主义在斗争，我们无法准确地判断哪些争论是来自主义本身，哪些争论又是来自争论所引起的激情。<br />
<br />
而在美国，人民毫无阻碍地统治着国家，（由于地缘环境的优越）他们既没有什么危险要担心，又没有什么损害需要报复。”<br />
<br />
以及，汉娜.阿伦特《论革命》：<br />
<br />
（美国代议制）决不会向多数人开启政治领域，也不会在他们当中激起“追求独特性的激情”――“不仅渴望平等或相似，而且渴望超越”的激情。亚当斯相信，政府的根本目的是管制“追求独特性的激情”。<br />
<br />
……而在大陆革命中，十九、二十世纪的革命家是绝望的人，于是革命事业吸引了越来越多的的绝望者，“一类不幸的人，在常规政府的缓和期沦落到非人的境况，在国内革命杀气腾腾的场面中，他们崛起而具备了人的特征。”<br />
<br />
……恰恰是法国大革命释放出来的激情和观念，窒息了本来的革命精神――――那本来鼓动了行动者的“公共自由、公共幸福、和公共精神”的原则。<br />
<br />
读书至此，汗颜而湿背。<br />
<br />
汉娜阿伦特也说过，“人们经常被革命者那罕见的无私所感动。”然而，无私，是不是意味着正确？到我这里就是，这种“追求超越的激情”，对于那不幸被这种激情盯上的对象――共产党来说，是否公平？或者说，有没有不公的可能？<br />
<br />
“最大的敌人，就是‘必须有敌人’这种观念。”把落后一面当作一个敌人来打倒，这种打共产党那里继承来的战斗精神，是不是一种错误的豪情？<br />
<br />
我这会儿才明白，许志永在（王荔蕻转进来的）八月三十一号、九月一号两封信中对我的严厉批评，他不是权术，他是真生气。<br />
<br />
早就知道民主的要义是谈判、是妥协，可，为什么事情变成了这个样子？是因为七月十四号之后政府那霹雳惊雷的连环辣手吗？“我这么温和的领袖都要被抓，你们这个破政府，不可交流、不可说服、不可谈判、不可救药、狼心狗肺、丧尽天良！――”<br />
<br />
来责问我的官员被我指着鼻子大骂，被骂得目瞪口呆。<br />
<br />
可，八月二十三号许志永出狱之后，当这个政府显示出可以交流、可以说服、可以谈判、可以救药的时候，你为什么还不肯放弃绝食，为什么非要用绝食来逼着对方承认通讯自由、人身自由这些道理呢？非要大获全胜呢？<br />
<br />
气节，对吗？<br />
<br />
被胜利冲昏了头脑，对吗？<br />
<br />
急着自由，急着在英雄凯歌中返回北京，被自己想象的情景感动了？对吗？<br />
<br />
却不肯考虑一下政府决策机制的笨重，不肯考虑一下政府官员们的局限和惊恐。或者说，被你自己不幸而言中：双方陷入了互相惊吓、互相激怒的怪圈！<br />
<br />
可，究竟是谁先惊吓了谁？谁先激怒了谁？为什么要下那样的狠手？<br />
<br />
是公盟两次“丧尽天良”的指责――姚晶案和邓玉娇案时的指责――惊吓激怒了政府吗？可姚晶和邓玉娇受的欺负就活该？<br />
<br />
是我的“江姐、许云峰”惊吓和激怒了当局吗？可，在层层打手和凶恶官员的呵斥下，在一天一天的绝食昏黑中，我不拿这份英雄豪情来顶，我如何顶得过这么多艰苦的日子？<br />
<br />
而，最让我担心的是，我担心许志永在北京在监狱里，在那么艰苦的条件下，他也得激起这样的英雄豪情来顶，我担心出狱后的许志永，从法律人变成了英雄！许志永变成英雄，那可真是中国民主的一大损失！<br />
<br />
那我的示威、绝食、责骂政府――所有苦心都白费了！我激进是为了让他保持温和，所有的烈火，都是为了保护一团白云。可，他的温和能保持多久？那一团白云能经得住多少摧残？他也是人，也是有血有肉、打了会疼的人！――<br />
<br />
就算是他这一次关得短、没挨打、他保持了温和，下一次呢？下下一次呢？一关几年呢？一关几年的话，他不拿出一份英雄豪情来，不拿出一份坚强和骄傲来，他如何顶过监牢里那艰苦的日子？<br />
<br />
而，许志永的坚强和骄傲，只会是民主的一项负资产！几年监牢在他身上所刻下的伤痕，所逼出的坚强和骄傲，会让他不自觉地蔑视他的后代儿孙、后来的竞争者：想上台，也拿几年大牢来换！――<br />
<br />
这样的许志永就反动了。<br />
<br />
我们把从父辈那里传承的枷锁又传承给了儿孙，“敌人”有多反动，我们就有多反动。<br />
<br />
唐小昭对“刘沙之英雄气”的理论批判，刘沙深以为然。（她的错误，主要出在证据部分）这也是为什么我在各坛子，对小昭退避三舍。<br />
<br />
而慧文也曾劝过我：“如果你真去示威绝食了，志永知道了，也许会欣慰，但欣慰之后他会悲哀的：连一个愿意‘用生命来担保我’的支持者，都不理解我的理念――志永会悲哀的。”<br />
<br />
慧文说错了。<br />
<br />
许志永，我理解你。温和、理性、法治，我样样都理解，样样都支持你去做。然而，我不能也去做。<br />
<br />
你有多温和，我就得有多尖锐。<br />
<br />
我当然知道这类英雄豪情对中国民主是一剂毒药。可乌云密布、爪牙四出、大家都在畏缩观望时，必须有一剂火辣辣的毒药来刺痛大家！<br />
<br />
兰志学在六月底，就曾对我的上一次绝食不以为然，劝我：“身为一个公众人物，你应该知道，什么时候该做什么，什么时候做什么是最合适的。”<br />
<br />
啊对不起兰兄，我不是什么公众人物。刘沙就一性情女子！精明算计、把握时机这等领袖任务，非刘沙力所能及。还是留给许志永那个丫去做吧，他就这么个吃苦耐劳的命！――我现在的工作方法，就是匹夫之勇、妇人之仁！我能起的作用，就是鼓舞士气、凝聚人心！<br />
<br />
你们男人，你们律师，在办一件案子的时候，是拿它当一件事务来办的。而我在办一件案子的时候，是拿它当一份疼痛来办的。所以我为怀阳痛，为宁哥痛，为许志永痛得眼前发黑，失去思考的能力。<br />
<br />
很傻对不对？可，不疼痛的刘沙，不是刘沙。<br />
<br />
男人们的任务是理智地行动，而女人们的任务是，通过这份疼痛，把大家凝聚在一起，凝聚成骨肉相连、十指连心的兄弟姐妹。<br />
<br />
还有，三尺之冰，非一日之寒也；三代之际，非一人之力也。一场波澜壮阔的民主运动，不可能只有一种理念、一种声音！<br />
<br />
郭泉的激进言行，看得我目瞪口呆，冷汗热汗“刷刷”的一齐往外冒！我说什么也不赞同郭泉对共产党的毒辣批评，然而，我对郭泉，心存感激。<br />
<br />
因为，有他这样的激进派冲在前边，温和派的日子会好过一点。<br />
<br />
有血性没韧性的刘沙，反正是不配活下来建设新中国的。而许志永这个女人一般心细的温吞水，他得承担最艰难的任务，他得活下去谈判。 <br />
<br />
再说啦，逞英雄出风头的事体有刘沙一个人就够了，许志永也凑热闹当英雄，实在烦人多余――<br />
<br />
（调侃）我自己是女英雄，我就讨厌男英雄；我自己是“女烈士”，我就讨厌“男烈士”，我自己是文学女青年，我就讨厌文学男青年！<br />
<br />
而，糟糕的是，许志永平常没事还能保持法律人的冷静，可一旦遭受沉重打击，这丫那文章，《苍天在上》什么的，活活儿就一文学男青年！ &gt;_&lt;||<br />
<br />
我要一个公民领袖用以追随，我要一个文学男青年有什么用啊？！天哪！…………<br />
<br />
笑完了，说正经的：从公来讲，为国家计为民族计，不希望中共对许志永太残酷了，不希望他被逼成英雄！<br />
<br />
从私心计，我心坎上的宝贝，洁白漂亮的宝贝，我要他在公众的视线下，完完整整的，平平安安的，一根手指头的危险都不许有，一根头发的危险都不许有！<br />
<br />
而且，我说什么也信不过GCD的监狱，打死也信不过GCD的监狱！共产党的规律就是没有规律，有高智晟和郭飞雄的遭遇在前边，许志永一坐牢，那就意味着一切危险、一切残酷、一切皆有可能！<br />
<br />
八月五号派出所的昏暗屋子里，赵连海劝我进食，劝我接受现实：“许志永坐牢是必然的――”“必然”二字一撞，刘沙眼前一黑，如撞南墙、如坠断崖，眼前一片血红！――没有理智、没有感情、只有画面、画面是一片血红！血红之上，只有一个字：不！！！――――<br />
<br />
所以，我把狠话撂在这儿了：对许志永――你丫不许去坐牢，你要再敢逞英雄去坐牢，我死给你看！对官方――想抓许志永容易，从我的尸体上踩过去！<br />
<br />
总之，事涉许志永的问题，大家可以讲讲道理。事涉许志永坐牢的问题，谁也别和我讲任何道理！</div>

							
							
							
							
