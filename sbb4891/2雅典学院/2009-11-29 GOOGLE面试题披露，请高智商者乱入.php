																											<div id="postmessage_349244" class="t_msgfont">据国外媒体报道，Google人力资源部门面试的时候都会询问面试者一些问题，但是在这些问题之中有些简单的让人摸不着头脑，（比如说，为什么窨井盖是圆的？）有些却让人感觉到很傻。现在罗列出其中最具特色的十五道问题并附上答案和大家共享。<br />
<br />
1.一辆校园巴士可以装多少个高尔夫球?<br />
<br />
面试职位：产品经理<br />
<br />
注解：Google公司提问这道问题的目的在于测试面试者是否可以找到解决问题的关键点。<br />
<br />
下面是一位面试者马特（Matt Beuchamp）给出的绝妙答案：<br />
<br />
首先，我要指出的是，我给出的答案是在一辆静止的标准校园巴士上去装高尔夫球。这辆校园巴士8英尺款，6英尺高，20英尺长。也就是说，这辆校园巴士的总体体积为960立方英尺，共计160万立方英寸。按照一个标准高尔夫的半径是0.85英寸来计算，一个高尔夫球的体积是2.5立方英寸。这样，计算的结果是，这辆校园巴士可以装下66万个高尔夫球。但是，校园巴士里面还有座椅等物体也会占据一定的空间，在加上高尔夫球本身是圆的，所以高尔夫球之间也会有一定的空隙。这样我最后的预计是会装下50万个高尔夫球。如果按照布什总统等名人的单独校园巴士来计算的话，我会把数量减半，也就是可以装下25万个高尔夫球。<br />
<br />
2.清洁完西雅图市的所有窗户需要花费多少美元？<br />
<br />
面试职位：产品经理<br />
<br />
答案：这个问题其实就是一道脑筋急转弯的问题，答案是“每扇窗户10美元”。<br />
<br />
3.在一个国家里，人们都想有个儿子，每个家庭都会不断的生孩子，直到有个儿子为止。那么这个家庭的男女比列会是多少？<br />
<br />
面试职位：产品经理<br />
<br />
答案：这是个很容易有争议的问题，但是标准答案解题步骤如下：<br />
<br />
1.假设现在这个国家有10对夫妇，他们共有10个孩子。其中这10个孩子中5个男孩，5个女孩。<br />
<br />
2.那么其中5对孩子是女孩的夫妇会继续生孩子。按照概率论来说，将会有2.5个男孩，2.5个女孩。加上原有的10个孩子，现在共有15个孩子，其中7.5个男孩，7.5个女孩。<br />
<br />
3.接下来，其中有2.5对夫妇仍未有儿子，将继续生孩子。按照概率论，将会在增添1.25个男孩，1.25个女孩。加上原来的孩子，现在共有17.5个孩子，其中8.75个是男孩，8.75个是女孩。<br />
<br />
依次类推，最后男女比例为1：1.<br />
<br />
4.世界上有多少位钢琴调音师？<br />
<br />
面试职位：产品经理<br />
<br />
答案：这个问题主要看市场支配的参量是什么。如果世界上每个钢琴每周需要调音一次，每次调音需要1个小时，一位调音师每天工作八个小时，每周工作五天，则每周一个调音师可为40架钢琴调音。而世界上恰好只有40架钢琴的话，那么答案就是世界上只需要一个钢琴师为40架钢琴调音。<br />
<br />
5. 为什么窨井盖是圆的？<br />
<br />
面试职位：软件设计工程师<br />
<br />
答案：这个问题是个考验面试者创造力的问题，没有什么固定的答案，只要你能将这个问题解释的通就可以。<br />
<br />
6. 你怎样为旧金山市设计一个合适的城市居民撤离计划？<br />
<br />
面试职位：软件设计工程师<br />
<br />
答案：这个问题的目的还是要在于测试面试者如何处理棘手的问题。可以将其转化为：“面对灾难性问题，你的准备计划是什么？”<br />
<br />
7. 时钟的指针一天内会重合几次?<br />
<br />
上午：<br />
<br />
12:00 ，1:05 ，2:11，3:16 ，4:22， 5:27， 6:33， 7:38， 8:44 ，9:49 ，10:55<br />
<br />
下午：<br />
<br />
12:00， 1:05， 2:11， 3:16， 4:22， 5:27， 6:33， 7:38， 8:44， 9:49，10:55<br />
<br />
8. 名词解释“dead beef”？<br />
<br />
面试职位：软件设计工程师<br />
<br />
答案：dead beef 有两种解释，一种是表面意思即“死牛”，另一种意思是计算机专业属于“中断”：系统停止当前正在运行的程序而转向其他服务，可能是因为优先级高的请求服务了，或者是因为人为安排中断。中断是属于正常现象。<br />
<br />
9. 一个人在失去所有的资产后把车停到酒店中，接下来会发生什么？<br />
<br />
面试职位：软件设计师<br />
<br />
答案：他被赶出停车场，只允许将车停在大路上<br />
<br />
10. 你想检查你的朋友BOB是否记住了你的电话号码，但是你又不可以直接去问BOB。你可以在制片上写一个问题给EVE，然后让EVE将卡片带给BOB，后让BOB将答案写在纸片上带给你。问，在制片上除了写明问题之外，还需要写上什么才能使得BOB把答案加密使EVE看不到你的电话号码，而你却可以了解BOB有没有记住你的电话号码？<br />
<br />
面试职位：软件设计工程师<br />
<br />
答案：你的目的在于检查BOB是否记住了你的电话号码，所以，只需要在卡片上写明，让BOB在固定的时间打电话给你就能确认。答案很简单？但是有人的回答确是：让BOB回答的卡片上写出你所有的电话号码数字之和，然后让EVE带给你，看最终结果是否正确。<br />
<br />
11. 你现在是海盗船的船长，船员们正在等着你分配抢夺来的黄金。如果高于半数的船员不同意你的分配方案的话，你会被杀死。问，你是如何在不被杀死的情况下将黄金分到的最多？<br />
<br />
答案：用最低的代价使得51%的船员同意即可.<br />
<br />
12. 你有8个一样大小的球，其中7个的重量是一样的，另一个比较重。怎样能够用天平仅称两次将那个重一些的球找出来？<br />
<br />
答案：将八个球先取出六个来，分别在天平的两侧放上三个。如果平衡，那就证明比较中的那个球在剩下的两个球里。在把剩下的两个球放在天平的两侧，就可以找出最重的那个球。如果不平衡，那么最重的那个球就在向下倾斜的那三个球里。在从这三个球中，取出两个称量，如果天平平衡，那么剩下的一个就是最重的球。如果不平衡，那么向下倾斜的就是最重的球。<br />
<br />
13. 你拿着两个鸡蛋站在在一栋100层的大楼前。这两个鸡蛋或许很结实，就算是从100层大楼的楼顶掉下也不会摔破，也或许很易碎，在第1层楼摔下就破碎。在两只鸡蛋都被打碎以前，你用什么方法可以找出鸡蛋不会被摔碎的最高楼层？<br />
<br />
面试职位：产品经理<br />
<br />
答案：对于100层的大楼来说 ，最多只需做19次实验就可以得出结果。<br />
<br />
首先，先每隔十层楼将鸡蛋丢下，从10层、20层、30层……依次丢下一个鸡蛋。这样最差的情况是在第100层楼上，鸡蛋被摔碎。然后，从第91层开始每隔一层丢一次，最差的情况是到第99层摔碎。这样，最多实验19次就可以找到鸡蛋不被摔碎的最高楼层。<br />
<br />
<br />
14. 你要向你8岁的侄子解释什么是数据库，请用三句话完成？<br />
<br />
面试职位：产品经理<br />
<br />
答案：这个题目主要是考察面试者怎样用简单的语言表达复杂的思维。我们的答案是：“数据库是用来记录许多信息的机器；人们使用数据库记录信息；并在需要的时候从里面提取出来。<br />
<br />
15. 你被缩小到只有硬币厚度那么点高(不是压扁，是按比例缩小)，然后被扔到一个空的玻璃搅拌器中，搅拌刀片一分钟后就开始转动。你怎么办?<br />
<br />
面试职位：产品经理<br />
<br />
答案：这个问题是为了测试面试者的创造性思维。正确回答：切掉电源。</div>

							
							
							
							
