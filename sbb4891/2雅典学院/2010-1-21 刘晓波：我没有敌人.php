													<h2>刘晓波：我没有敌人</h2>
						
						
																											<div id="postmessage_444174" class="t_msgfont">在我已过半百的人生道路上，1989年6月是我生命的重大转折时刻。那之前，我是文革后恢复高考的第一届大学生（七七级），从学士到硕士再到博士，我的读书生涯是一帆风顺，毕业后留在北京师范大学任教。在讲台上，我是一名颇受学生欢迎的教师。同时，我又是一名公共知识分子，在上世纪八十年代发表过引起轰动的文章与著作，经常受邀去各地演讲，还应欧美国家之邀出国做访问学者。我给自己提出的要求是：无论做人还是为文，都要活得诚实、负责、有尊严。那之后，因从美国回来参加八九运动，我被以“反革命宣传煽动罪”投入监狱，也失去了我酷爱的讲台，再也不能在国内发表文章和演讲。仅仅因为发表不同政见和参加和平民主运动，一名教师就失去了讲台，一个作家就失去了发表的权利，一位公共知识人就失去公开演讲的机会，这，无论之于我个人还是之于改革开放已经三十年的中国，都是一种悲哀。<br />
想起来，六&#8226;四后我最富有戏剧性的经历，居然都与法庭相关；我两次面对公众讲话的机会都是北京市中级法院的开庭提供的，一次是1991年1月，一次是现在。虽然两次被指控的罪名不同，但其实质基本相同，皆是因言获罪。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; 二十年过去了，六&#8226;四冤魂还未瞑目，被六&#8226;四情结引向持不同政见者之路的我，在1991年走出秦城监狱之后，就失去了在自己的祖国公开发言的权利，而只能通过境外媒体发言，并因此而被长年监控，被监视居住（1995年5月-1996年1月），被劳动教养（1996年10月-1999年10月），现在又再次被政权的敌人意识推上了被告席，但我仍然要对这个剥夺我自由的政权说，我监守着二十年前我在《六&#8226;二绝食宣言》中所表达的信念――我没有敌人，也没有仇恨。所有监控过我，捉捕过我、审讯过我的警察，起诉过我的检察官，判决过我的法官，都不是我的敌人。虽然我无法接受你们的监控、逮捕、起诉和判决，但我尊重你的职业与人格，包括现在代表控方起诉我的张荣革和潘雪晴两位检察官。在12月3日两位对我的询问中，我能感到你们的尊重和诚意。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 因为，仇恨会腐蚀一个人的智慧和良知，敌人意识将毒化一个民族的精神，煽动起你死我活的残酷斗争，毁掉一个社会的宽容和人性，阻碍一个国家走向自由民主的进程。所以，我希望自己能够超越个人的遭遇来看待国家的发展和社会的变化，以最大的善意对待政权的敌意，以爱化解恨。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;众所周知，是改革开放带来了国家的发展和社会的变化。在我看来，改革开放始于放弃毛时代的“以阶级斗争为纲”的执政方针。转而致力于经济发展和社会和谐。放弃“斗争哲学”的过程也是逐步淡化敌人意识、消除仇恨心理的过程，是一个挤掉浸入人性之中的“狼奶”的过程。正是这一进程，为改革开放提供了一个宽松的国内外环境，为恢复人与人之间的互爱，为不同利益不同价值的和平共处提供了柔软的人性土壤，从而为国人的创造力之迸发和爱心之恢复提供了符合人性的激励。可以说，对外放弃“反帝反修”，对内放弃“阶级斗争”，是中国的改革开放得以持续至今的基本前提。经济走向市场，文化趋于多元，秩序逐渐法治，皆受益于“敌人意识”的淡化。即使在进步最为缓慢的政治领域，敌人意识的淡化也让政权对社会的多元化有了日益扩大的包容性，对不同政见者的迫害之力度也大幅度下降，对八九运动的定性也由“动暴乱”改为“政治风波”。敌人意识的淡化让政权逐步接受了人权的普世性，1998年，中国政府向世界做出签署联合国的两大国际人权公约的承诺，标志着中国对普世人权标准的承认；2004年，全国人大修宪首次把“国家尊重和保障人权”写进了宪法，标志着人权已经成为中国法治的根本原则之一。与此同时，现政权又提出“以人为本”、“创建和谐社会”，标志着中共执政理念的进步。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;这些宏观方面的进步，也能从我被捕以来的亲身经历中感受到。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;尽管我坚持认为自己无罪，对我的指控是违宪的，但在我失去自由的一年多时间里，先后经历了两个关押地点、四位预审警官、三位检察官、二位法官，他们的办案，没有不尊重，没有超时，没有逼供。他们的态度平和、理性，且时时流露出善意。6月23日，我被从监视居住处转到北京市公安局第一看守所，，简称“北看”。在北看的半年时间里，我看到了监管上的进步。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;1996年，我曾在老北看（半步桥）呆过，与十几年前半步桥时的北看相比，现在的北看，在硬件设施和软件管理上都有了极大的改善。特别是北看首创的人性化管理，在尊重在押人员的权利和人格的基础上，将柔性化的管理落实到管教们的一言一行中，体现在“温馨广播”、“悔悟”杂志、饭前音乐、起床睡觉的音乐中，这种管理，让在押人员感到了尊严与温暖，激发了他们维持监室秩序和反对牢头狱霸的自觉性，不但为在押人员提供了人性化的生活环境，也极大地改善了在押人员的诉讼环境和心态，我与主管我所在监室的刘峥管教有着近距离的接触，他对在押人员的尊重和关心，体现在管理的每个细节中，渗透到他的一言一行中，让人感到温暖。结识这位真诚、正直、负责、善心的刘管教，也可以算作我在北看的幸运吧。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 政治基于这样的信念和亲历，我坚信中国的政治进步不会停止，我对未来自由中国的降临充满乐观的期待，因为任何力量也无法阻拦心向自由的人性欲求，中国终将变成人权至上的法治国。我也期待这样的进步能体现在此案的审理中，期待合议庭的公正裁决――经得起历史检验的裁决。<br />
<br />
