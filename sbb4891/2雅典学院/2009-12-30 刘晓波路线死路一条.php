新婚没几天，内弟就劝说淑女妻子赌牌打麻将，在传授麻将技术方面极有耐心，手把手教导妻子由浅入深。我问内弟为何要这样做？这不是给自己和家庭找麻烦吗？一个“败家子”就受不了还要摊上俩？没想到内弟用一句“你不懂”封住了我的嘴，说什么不这样将来会有更大的麻烦？开始淑女弟媳怎么也“进不了状态”，一再宣称自己不是赌牌的料。可内弟在这方面的耐心让人吃惊，一次又一次地体验之后终于让弟媳找到了感觉，食髓知味后就进而爱不释手了。麻将这东西有“毒品效应”，一旦上了瘾就会身不由己。等到她终于发现赌牌毁人心术又败坏家庭时，她已经欲罢不能了。<br />
<br />
两年后的春节我去内弟家拜年，吃惊的发现那位淑女弟媳和刚过门时判若两人，粗话脏话脱口而出，人生品味价值取向越来越象内弟？两年前我眼中的弟媳是标准的阳春白雪，现在则坠落成货真价实的下里巴人了。<br />
<br />
内弟的家庭让我想起了秦桧和他的妻子王氏？<br />
<br />
内弟的婚姻保住了，但他们的家庭也因此永远失去了希望。儿子成了青出于蓝而胜于蓝的流氓混混；女儿我就不说了，免得损口德。<br />
<br />
…………<br />
<br />
国家民族很多时候象极了“美丽的淑女”，不可能总是“有德者居之”。历史老人也常常开些让人幽默不起来的玩笑，“鲜花插在牛粪上”的乱点鸳鸯谱一而再再而三让史学家心情沉重。“贱民朱三也天子”，流氓统治者长期绑架淑女国家的悲剧前赴后继。朱三多少还有点良知，只顾自己爬灰玩儿媳妇，没有同时诱使整个国家堕落。结果他的江山满打满算也不到二十年。法兰西帝国的路易十四、路易十六就比他老兄精明很多，自己穷奢极欲的同时还忘不了带上他的子民一道围着“权钱”打转，把自己的命运和国家民族的前途牢牢绑在一起。<br />
<br />
我们来看看托克维尔在他的论著《旧制度与大革命》一文中，描述法国大革命前的社会风貌，就可明了路易王朝的“堕落诱惑”术玩得多么成功。<br />
<br />
“在这类社会中，没有什么东西是固定不变的，每个人都苦心焦虑，生怕地位下降，并拼命向上爬；金钱已成为区分贵贱尊卑的主要标志，还具有一种独特的流动性，它不断地易手，改变着个人的处境，使家庭地位升高或降低，因此几乎无人不拼命地攒钱或赚钱。不惜一切代价发财致富的欲望、对商业的嗜好、对物质利益和享受的追求，便成为最普遍的感情。这种感情轻而易举地散布在所有阶级之中，甚至深入到一向与此无缘的阶级中，如果不加以阻止，它很快便会使整个民族萎靡堕落。然而，专制制度从本质上却支持和助长这种感情。”<br />
<br />
 <br />
<br />
但这个成功的果实太过苦涩了！法国因此爆发了欧洲历史上最残酷的暴力革命，路易家族和整个特权阶层全被送上了断头台。堕落的国民只会玩毁灭一切暴力革命那一套；绝不会玩上下双赢的理性政治变革。淑女国家也因此付出了惨重的代价。这个欧洲大陆无与伦比的超级强国，这个造就了近代世界多数大思想家的伟大国度，在拿破仑后长达一个半世纪的现代史时期一直成为被动挨打的出气筒和文化沙漠思想侏儒。<br />
<br />
 <br />
<br />
淑女妻子千万别委身于流氓丈夫。流氓丈夫一旦骗到了淑女妻子，最好也别诱惑淑女堕落，那是和自己家族的未来过不去。</div>

							
							
							
							
