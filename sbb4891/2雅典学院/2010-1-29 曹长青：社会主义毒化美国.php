													<h2>曹长青：社会主义毒化美国</h2>
						
						
																											<div id="postmessage_457198" class="t_msgfont">奥巴马当上总统才半年多，就要把美国带向社会主义：要巨额扩大政府开支，扩大福利，扩大财政赤字，政府要管医疗保险，管教育，管企业，甚至连大汽车制造厂，政府也要成为最大股东。奥巴马任命的几十个如同钦差大臣般的“主管”，被媒体嘲讽地称为“沙皇”。 <br />
<br />
这种高福利、大政府的政策，前景会怎么样？在美国，加州就是一个失败的典型。加州是美国人口最多、经济实力最强的州，GDP在全球排名第八（相当于整个法国）。但是，加州的财政赤字却已高达243亿美元，州政府几乎要破产关门。加州所以出现这种灾难，主要在于州议会一直掌握在左派民主党手里，他们主张大政府，高福利，扩大政府开支，由此造成财政黑洞，连曾在好莱坞扮演悍将的共和党州长阿诺，也束手无策。 <br />
<br />
但美国第二大州（无论人口还是土地面积）的德克萨斯，则和加州完全不同，其州议会共和党议员占多数（州长也是共和党），所以实行了小政府、低税收、充分市场经济的政策，结果德州不仅没有债台高筑，反而还有财政盈余，全州的失业率，还比美国整体低百分之二点三。英国《经济学人》杂志最近还以专题“孤单的上升之星”（Lone Star Rising）来报导德州的经济成就。 <br />
<br />
<br />
活着的马克思 Vs.当代哈耶克<br />
<br />
已经有加州的失败，德州的成功，两相比较优劣如此明显，但为什么奥巴马还是要走社会主义的道路？主要因为，社会主义理念是西方左派政党的立党之本，更是左派知识份子想通过“均贫富”实现“社会平等”的乌托邦理想。他们为理论、理想活着，闭眼不看现实。 <br />
<br />
高福利政策和左派知识份子有直接关系，他们不断著书立说，鼓吹均贫富、社会平等。当然也有右翼学者对它的反驳。几年前逝世的哈佛大学两位知名教授罗尔斯（John Rawls）和诺齐克（Robert Nozick），就是这两种理论的代表学者。 <br />
<br />
曾有一位中国教授来美国，和我谈论起美国的左右派，问我怎么看罗尔斯和诺齐克，并说中国学界对这两人相当重视。为了一下子让他明白这两人的区别，我用了简单的比喻：罗尔斯是活着的马克思，诺齐克是当代的哈耶克。虽然和马克思主张公有制不同，罗尔斯推崇私有制（从学术角度还有其他不同），但在强调“社会正义”这个大范畴上，其代表作《正义论》的主要观点和马克思有相当的重叠之处。他也主张均贫富，认为政府以“善”的名义，可以剥夺他人财产，进行二次分配，“善大于权利”。诺齐克则写出反驳罗尔斯的专著《无政府、国家与乌托邦》，强调个人的权利，主张限制政府权力，认为“权利大于善”。 <br />
<br />
罗尔斯的社会正义想法，是从卢梭、马克思、凯恩斯这么一路下来的。而诺齐克则继承了洛克、亚当斯密、哈耶克、弗里德曼这些重视个人自由者的思想。哈佛的这两位教授典型地代表了当今美国以至西方的左派、右派在福利等经济理念上的根本分歧，一个强调平等（集体福利），另一个强调自由（个人权利）。怎样看待平等和个人权利之间的关系呢？让我们以纽约地铁上的情景做个比较： <br />
<br />
劫富济贫的罗宾汉最不道德 <br />
<br />
在地铁上，时常会有乞讨者，虽然多数时候这些人看来都没有什么残疾。如果你发善心，给他几枚硬币或一美元，他会说声“上帝保佑你”表示感谢，你则通过施舍获得一种道德感。但如果是有人持枪威逼所有乘客，每人必须向这个乞讨者捐献一美元，情况则不同了，那个乞讨者不会再感激你，他会觉得这是社会公义，他应该在财富上获得和其他人的平等，并可能理直气壮地要求更多，而那些被迫“捐献者”也不再有原来的道德感觉，因为这和被抢劫在本质上没有多大不同。 <br />
<br />
罗尔斯等左派主张的社会正义，就是这种通过政府的强迫性高税收，把中产阶级和富人的钱强行收缴来，再分给穷人。但诺齐克等右派对此强烈反对，他们认为，这种做法和集体主义在本质上是一样的，都是在社会公义、“善”的名义下，抢夺他人的私有财产，剥夺人的权利。诺齐克认为，视个人权利大于善，才会保住自由。如果接受善大于权利，那么政府、统治者等，都可用善的名义来侵蚀、剥夺个人的权利，最后人们就不会有自由。例如法国大革命抢劫富人、列宁斯大林的集体农庄、毛泽东的湖南农民痞子运动等等，都是这种打土豪分田地、以善的名义剥夺个体权利的模式，来推行集体主义，最后走向极权。因而美国客观主义哲学的创办人、热烈赞美资本主义的思想家安・兰德（Ayn Rand）说，劫富济贫的罗宾汉是文学作品中最不道德的形象，因为他用最恶劣的方式――抢劫――来均贫富；而不是用市场交换成果来扩大人的财富和自由。 <br />
<br />
没有了个人权利的保障，所谓的社会之善，一定是伪善，是以集体吞噬个人的藉口。社会之善不是不需要，但应像纽约地铁的乞讨一样，施舍者是在拥有个人权利的基础上自愿选择去做的，而非被强迫。施舍者当然体现出某种道德，但不施舍是中性的，并不是缺德；但强迫别人掏钱养懒汉，则一定是不道德的。高税收就是政府强迫施舍（不缴税会犯法、坐牢）。所以右派强调小政府、大社会，主张通过社会上的慈善组织等进行救济；政府规模以及不得不实行的福利，都要尽量缩到最小的程度。 <br />
<br />
左派喜欢穷人，结果造出更多穷人 <br />
<br />
而左派知识人则在均贫富上走得相当远，罗尔斯甚至提出，个人的天赋等“自然优势”也不属于个人所有，而应被视为公共资源。等于也应该被共产，再分配，补偿给那些处境最差的人群。诺齐克曾对此愤怒地诘问：如果你碰巧幸运地有两只明亮的眼睛，是否应当捐献一只给双目失明的盲人才是公平？按照罗尔斯的理论，像乔丹、姚明等，就得被砍下一截腿，接到矮子脚上；把盖茨的脑子挖出一半，填到傻子脑壳中，社会才算“平等”。那我们就再看不到精彩的球赛，也不会再有盖茨发明微软，人们恢复到踢毽子、打算盘的时代，人人平等，“皆大痛苦”。 <br />
<br />
