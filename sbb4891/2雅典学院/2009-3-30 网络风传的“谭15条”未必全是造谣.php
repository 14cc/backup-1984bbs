																											<div id="postmessage_65121" class="t_msgfont">一点五<br />
<br />
近日，绵阳市委书记谭力被提拔，公开的理由是“抗震救灾表现突出”。这则新闻很快成为各大网媒的头号热点，点击与跟贴一家更比一高，也让最广大中国网民再次想起了那张著名的笑脸。<br />
<br />
在对“谭提拔事件”的所有评论中，传播最为广泛、影响最为恶劣的，莫过于一个名叫《请中央为“谭15条”公开辟谣》的贴子。尽管那个贴子已被负责任的有关部门删掉，但结果只是让领导同志“眼不见为净”而已，稍凡有点网络技术含量的网民，没有几人不知道。眼下那东东依然在风传着，虽然很不礼面地动用了“地下”方式。<br />
<br />
这个贴子首发百度“绵阳”贴吧，作者叫“复旦单身本科男”。从个人的感受看，此贴作者完全可定性为网络高人，因为他深谙“造谣”的严重后果，因此在每一条的后面都附有查证路径。然而作者后期有“情绪不稳定”之嫌，部分条款应该可以“不被采信”。但倘若因此将其全盘否定，似乎也难以“平民愤”，至少，此贴前半部分，未必尽是“恶搞”。<br />
<br />
第1条为“一个养藏獒的书记”。作者说，“谭养藏獒，在绵阳早就是公开的秘密”，并指出“请到富乐山酒店了解”。我想这一条应该很好查究，是就是，不是就请“有关部门”给人民的谭书记“还一个清白”。<br />
<br />
第2、3、4条分别是“一个住别墅的书记”、“一个卖明信片的书记”、“一个大胆提拔情妇的书记”，从时下盛行的“人本”高度出发，这些似乎与谭书记个人爱好有关，属于它的私人空间，可以不予追究。纵使一小撮网民有想法，那也只怪他们“素质不高”。<br />
<br />
从部门职能上看，第13条“一个‘顺我者昌，逆我者亡’的书记”，疑似应该引起四川组织部门高度重视。谭力高就，是四川组织部柯尊平部长亲自到绵阳宣布的。此条列举谭力在绵阳以频繁调动干部来“树威”的几个事例，无一不让人大开眼界，如一个市经委主任的岗位，两年内换了五人次，同一个人一年内提拔三次，等等这些是否符合中央组织部的《干部工作条例》？干部提拔任用的档案，全部在组织部门手里，能否请柯尊平部长万忙之中，调阅一下？澄清一下？解释一下？作为组织部长，想必这样的干活也在情理之中。<br />
<br />
领导提拔，重的是“政绩”。第10条“一个浮夸造假的书记”中举的几个例子，如虚报工业增加值，“2006年、2007年都报的增长29%，而这两年绵阳工业用电量仅分别增长0.7%、1.5%”，都是在“用数字说话”，反过来则是极容易核查的，能否公开有关资料？人世间真有这样的发展奇迹么？<br />
<br />
腐败不可怕，资本主义也有腐败，可怕的是人民面对腐败无能为力，政权面对腐败无动于衷。为防止干部工作中的腐败，中央组织部今年还专门开通了一个举报网站，《请中央为“谭15条”公开辟谣》中列举的事项，算不算举报？能不能查处？胡锦涛总书记一再强调：“要把反腐倡廉建设放在更加突出的位置，旗帜鲜明地反对腐败”，作为党和国家重要职级部门的组织部，能不能身体力行？带头表率？</div>

							
							
							
							
