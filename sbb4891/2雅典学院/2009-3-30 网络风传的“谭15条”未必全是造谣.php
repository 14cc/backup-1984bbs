116.52.4.*&nbsp;&nbsp;<br />
<br />
&nbsp; &nbsp; 1、一个养藏獒的书记。谭力一到绵阳，就在他住的富乐山酒店4号楼的背后修了一个小房子，在里面养了价值十万元的藏獒。藏獒生病了，由中心医院的医生专程上山来给它输液打针。谭养藏獒，在绵阳早就是公开的秘密。（请到富乐山酒店了解）<br />
<br />
　　 2、一个住别墅的书记。谭力到绵阳后，占据富乐山酒店4号别墅达10个月之久，后来住到了科创园他买的一套别墅里；去年11月，又搬进由市委办公室在富临山庄临人工湖处以年租2.3万元租的一套别墅，加上水电气费，一年别墅费用5万多元，都由公家报销了。（请到富临山庄去看）<br />
<br />
　　 3、一个卖明信片的书记。谭力好摄影，现拥有价值60万元的镜头。兴力达集团老总张钧专门给他修了一间洗印照片的小工厂。去年底，谭力将他以“巴蜀山人”摄的“虎牙风光”制成明信片，由市委办公室向各区县、各部门兜售。每套12张，96元一套。绵阳市水务集团、商业银行各买了1500套，各用去10多万元；涪城区龙门镇党委花40000多元买了500套，至今无处报销。（请到这三个单位调查）<br />
<br />
　　 4、一个大胆提拔情妇的书记。谭力到绵阳不久，就看上了绵阳市电视台的一个节目主持人。2005年9月将其调到市委接待办任科长，这次又提拔为北川重建党工委群工部副部长（副县级）。<br />
<br />
　　 5、一个贪生怕死的书记。这次汶川大地震，北川县城死亡上万人，谭力迟迟不到救援现场，当天晚上七点多钟，到了安县永安，又半途而回。救援最紧急的前五天，他除了陪上级领导去以外，从未单独到过北川县城。（请查地震发生后谭力的行程）<br />
<br />
　　 6、一个拉大旗作虎皮的书记。地震发生后，绵阳市民因对谭力指挥不力议论纷纷，谭竟将邓小平女儿邓榕发给他个人的手机短信，在《绵阳日报》、《绵阳晚报》刊登，在绵阳电视台滚动播出，在绵阳广播电台反复广播。（请查5月下旬的《绵阳日报》、《绵阳晚报》）<br />
<br />
　　7、一个信口开河的书记。为了应对舆论抨击，谭力组织媒体大肆宣传他。但在回答记者提问时，破绽百出，闹出笑话，引起群众更大反感。比如“维修市委大楼要花几千万元”，而市委大楼根本不是危房；“绵阳损失公路几万公里”，而绵阳全部公路通车里程不足15000公里；担心群众“躺在受灾的温床上，比地震更可怕”，地震几分钟使数万人丧生，还有比地震更可怕的吗？（请看6月上旬的《南方周末》、《新京报》）<br />
<br />
　　 8、一个抓捕网民的书记。5月16日以后，网上对谭在地震中的表现和调来绵阳后的表现，以“笑脸门”为导火线，批评如潮。谭力竟动用公安，追踪网友，从5月26日到6月8日的十多天内，以妨害治安为由抓捕了23个批评他的网友。（可调阅绵阳市公安局《治安拘留决定书》档案）<br />
<br />
　　 9、一个把形象工程当成“政治任务”的书记。唐家山堰塞湖险情排除的第二天，即6月11日，他就主持会议，布置“会客厅”的拆迁和建设，并且强调是“政治任务”（请查6月12日《绵阳日报》）。而此时的绵阳上万伤员在医院救治，数十万群众在临时安置点，数十万学生在防震棚里上课，这些都不如“会客厅”重要和紧迫！谭力到绵阳大搞形象工程，请派人来看看耗资6亿元的绵江公路，耗资上亿装修的富乐山酒店，耗资上千万的兴力达广场，各耗资上千万的江油市方水乡白玉村、涪城区龙门镇小桥村、城郊乡戈家庙村。<br />
<br />
　　 10、一个浮夸造假的书记。举几个例子：（1）浮夸。2005年3月28日，谭力搞了一个大型招商活动，声称签约259亿元，现在到位资金还不到10亿元。（2）虚报工业增加值。对下强压，对上虚报。2006年、2007年都报的增长29%，而这两年绵阳工业用电量仅分别增长0.7%、1.5%（可查有关资料，要么请上级来总结绵阳工业节能经验）。（3）造假。2006年5月11日，省委常委、成都市委书记李春城率团到安县千佛山考察，谭力从市级机关和安县各调2000多人以过工会活动名义上山，假扮游客，并从盐亭县组织几十个人上山去卖小吃，晚上回来发生车祸，造成3人受伤。（李春城来一事，可查2006年5月12《绵阳日报》）<br />
<br />
　　 11、一个打人书记。去年党的十七大期间，谭力在绵阳财政花2000多万元租凭和装修用于他搞关系的北京富乐山酒店宴请客人后，因话不对味，他竟当着十几个人的面动手打了市委秘书长和绵阳市委办公室副主任。<br />
<br />
　　 12、一个独断专横书记。谭力集大权小权于一身，除了他，无论大事小事，谁说了都不算，连市政府常务会的议题、时间、列席人员都要报经他点头才能召开。人们戏称，绵阳是“无政府主义”，唐利民市长是“唐科长”。<br />
<br />
　　13、一个“顺我者昌，逆我者亡”的书记。谭力来绵阳的三年多，以频繁变动干部来树立“权威”。请看赵琪的情况：2005年9月，将赵由北川县委书记调任三台县委书记，2006年6月调任市国资委主任，2007年2月调任市委副秘书长，2007年12月任市政府秘书长，2008年7月任市委秘书长。谢晓东：2005年9月由市委组织部部务委员、干部二科科长提任副部长，2005年12月任绵阳市礼宾局局长，2006年6月任涪城区区长，9个月变动三次。请看市经委主任变动情况：主任王黎2005年9月调任安县县委书记，由邓坤接任主任，2006年6月邓调燃气公司董事长，由刘联山主持工作，2006年12月，魏德谦任主任，2007年5月，刘联山任主任。两年时间内一个单位的一把手换了五人次！（请查绵阳市经委干部档案）<br />
<br />
　　 14、一个“网络红人”书记。谭力因“笑脸门”为导火线，引发网上抨击。在他运用关系大肆封杀和删贴的情况下，网上抨击谭力的贴子仍超过45万条，开创有网络以来市委书记之最。（请进百度搜索去看）<br />
<br />
　　 15、一个怨声载道、骂声不绝的书记。谭力来绵阳不久，民间就流传着许多他的绰号和顺口溜：“谭壳子”（吹牛的意思）、“谭盒子”（神经有问题）、“谭绷子”（一天都板起面孔）、“走了个酒疯子（指黄学玖酒后开车被免职），来了一个谭盒子”、“谭力不走，绵阳人民要讨口”。街谈巷议，对谭力骂声不绝。公道自在人心，谭力网上当“红人”，决非偶然。这种“老鼠过街，人人喊打”的干部，全国可能找不到第二个。</div>

							
							
							
							
