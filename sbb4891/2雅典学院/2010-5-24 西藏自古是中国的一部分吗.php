																											<div id="postmessage_636778" class="t_msgfont">自由亚洲电台 （自由亚洲电台记者北明采访）<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 北明：一直以来，海峡两岸的中央政府都宣称，西藏是中国的领土一部分。大部分汉人对此一说法耳熟能详，从不怀疑。即便怀疑，也缺乏足够的信息进行确证。另一个引人注意的事实、和导致中藏谈判破裂的重要因素是，达赖喇嘛和西藏流亡政府即便在寻求“中华人民共和国宪法框架下的真正自治”、认可西藏目前是中国领土一部分的时候，仍然坚持不能改变西藏曾是独立国家的历史事实。你作为长期关注并研究西藏问题的专家，如何看待中国官方和西藏流亡政府之间关于西藏历史归属问题上的不同？ <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 曹长青：达赖喇嘛在过去十几年来一直强调“中间道路”：不追求西藏独立，承认西藏属于中国，但要求西藏高度自治，藏人治藏。北京当局则要求达赖喇嘛必须承认，西藏自古以来就是中国的一部分。而达赖喇嘛认为，历史事实不是这样。我曾在一个欧洲会议上，直接听到坐在台上的达赖喇嘛说：我是一个和尚，和尚的舌头不能说假话。他说，事实是，西藏在历史上并不是中国的一部分。 <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 那么在这个问题上，到底谁对、谁错？其实只要我们回顾历史，就会看到，北京当局的说法至少有两个错误：一是不符合历史的真实；二是不符合逻辑常识。 <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 西藏历史上是独立国家 <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 首先我们来看历史真实：中国历史上有五个大的朝代：唐、宋、元、明、清。在唐朝时，西藏不仅是一个国家，还是一个强大的国家。西藏吐蕃王松赞干布兼并了各部落为统一的国家，并扩展疆土。西藏的军队还曾一度占领了唐朝的首都长安。很多中国人知道文成公主的故事，也就是把她送去给西藏国王松赞干布做妃子，寻求双方和好。唐朝和西藏，双方在长安还建了一个碑，说大家以后再也不要互相侵犯了，咱们是亲戚了。长安附近的这个碑还在呢，这些史事都证明，西藏原来是一个独立的国家！ <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 北明：可是后来中国政府说，这个文成公主恰恰证明了西藏和中国的密切关系，就证明了西藏属于中国。 <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 曹长青：我们从常识逻辑角度，你把自己的女人送给另一个国家的国王做妃子，你是强大还是软弱呀？你强大怎么把女人给送去呢？而且文成公主愿意吗？去西藏高原，一个她没见过、不知道是怎么样的人，就是她丈夫了，而且是另外一个族群、另外一块土地、另外一种气候！是你被迫嘛！因为你打不过人家嘛！<br />
&nbsp; &nbsp; 　 <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 北明：这种两族的婚姻能不能说明西藏的归属？ <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 曹长青：当然不能！两族通婚怎么就是归属关系呢！今天西方哪个国家，会说我把女人送给你这个国家元首做太太，你就是我的一部分了，这是什么逻辑呀？荒唐嘛！今天中国任何一个历史学家，包括研究西藏的牙含章们，大家过来讨论一下，你说那个唐朝长安被占领，是谁占领的？西藏是不是一个国家？难道是你国家的一部分把你自己国家的首都给侵占了？有这个逻辑吗？所以这是一个历史常识的问题！ <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 北明：您再谈一谈忽必烈跟..... <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 曹长青：好。唐宋元明清，唐朝后，就是宋朝。宋朝时，西藏和中国没什么关系，历史记载也较少。宋朝完了是元朝。在元朝时是有关系的。当时不论宋朝的领土，还是西藏，全都被蒙古人占领了！成吉思汗的铁骑遍及亚洲甚至欧洲。中国人把蒙古人在中国土地上建立的一个朝代，当成自己的一个历史朝代（元朝），这本身在逻辑上都是有点问题的。但即使这样，你怎么能说，蒙族人把两个地方都占了，你却说他们另外占领的那个地方，就是你的一部分呢？这在逻辑上怎么能说得通呢？何况蒙古人没有直接统治西藏，因为元世祖忽必烈尊奉藏人大喇嘛八思巴为整个蒙帝国的最高上师，相当于国师，让他主掌西藏政教权力，元朝时，藏人是自我管理的。今天，只有蒙族人出来说，你这两个地方都是我们的一部分，那还可能有一点点逻辑。 <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 北明：也就是说把蒙古人统治作为元朝，然后认为这是中国的历史，所以也就是说，蒙古人占领了西藏也就等于中国人占领了西藏？ <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 曹长青：如果北京当局的这种逻辑可以成立，那是不是可以说，俄罗斯是中国历史以来不可分割的一部分呢？因为蒙古人当时把大半个俄罗斯也都占领了。为什么你不说？你说不过人家，人家强大嘛。你为什么说西藏是你的一部分，因为西藏在你手里，你欺负人家，不讲理、不讲历史的真实嘛！ <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 元朝完了是明朝。明朝也跟西藏没有多少联系，跟宋朝时差不多，历史记载很少。互相有个册封呵，附庸风雅，我尊崇你是什么伟大皇帝，你尊崇我是什么上师法王，以这种方式，互相表示友好而已。 <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 西藏和中国的关系主要发生在清朝。有四次，清朝的军队进入了西藏。干什么呢？是应达赖喇嘛的要求来平乱和抵抗外敌的：因为尼泊尔入侵，内部和尚造反等等，那么我就请你清朝来帮我们平乱，平乱以后清朝军队就回去了。为什么清朝军队做这个事呢？因为双方有特殊的关系，叫施主和被施主。这是什么关系呢？就像今天的意大利和梵蒂冈的关系似的。梵蒂冈是天主教的圣地，最高领袖是罗马教皇。意大利作为世俗世界，对我们梵蒂冈又送水、又送药、又送吃的。由于对我们的尊崇，意大利境内的天主教徒们，就会信服意大利政府，使世俗社会更稳定。打个比方说吧，当时清朝和西藏的关系就像一个村子旁有个和尚庙，这个庙宇没有宣布过独立，因为没必要；而这个村子，也不强调庙宇是我们村的一部分。这是一种互为友好、互利互惠的关系。我们的村长带领村民尊奉和尚庙的喇嘛，给你送水送吃的，做施主。尊奉喇嘛，有助于村子的稳定，因为村民多是佛教徒，庙宇的喇嘛是村民的精神领袖。这是一种施主和被施、物质和精神的互惠关系。它不是一个典型的西藏是清朝的一个省、一个州、一个下辖市的关系，它不是这样的。 <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 我们看看满清皇帝顺治，那是清朝很强盛的时候，1652年，当时第五世达赖喇嘛，跟满清皇帝见面，顺治皇帝是出城40里迎接的。之前有汉臣说，你是一国之皇帝，而且是天下之国――全世界他都是最高中心了――你怎么可以去迎接一个喇嘛呢？但是顺治认为，达赖喇嘛是最高上师、佛教法王，为了我们大清的稳定，促使信佛教的外蒙归顺，必须迎接，给予高礼遇。<br />
&nbsp; &nbsp; 　　<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 北明：当时他还为了跟这些大臣能够说得通，找了一个借口。好像是他出游、打猎，假装遇上的，但是实际上他是有心要去迎接。 <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 曹长青：对，是这样的。我们看看，在清朝最腐败、无能的时候，来了个英国特使，清廷还要人家下跪呢！而在顺治统治的清朝强大时期，皇帝怎么可能亲自出城迎接一个外臣呢？虽然说我是打猎，路遇的，但这明显是变通方法，出城40里，就是一个顶礼迎接的方式。 <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 再有一个就是第十三世达赖喇嘛，就是现在达赖喇嘛的上一世，也跟中国皇朝的掌权者见过面，跟谁？跟慈禧。慈禧太后当时垂帘听政。后来还有一个关于这个场面的壁画呢，在中国的什么档案馆保存着。中国官方学者、西藏问题专家牙含章写的那本《达赖喇嘛传》，一打开就是这个壁画照片，是慈禧太后和第十三世达赖喇嘛并排坐在金銮殿上。中国哪个皇帝允许下面的郡主、大臣、将军，甚至外国的使臣什么的，和他并排坐在金銮殿上？不可以的，都是跪在下面的。 <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 北明：慈禧太后是个佛教徒。 <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 曹长青：从这些对达赖喇嘛的特殊礼节，也可以看出，西藏并不是满清王朝的下属。它不是一个上下级的从属关系，而是一个特殊的关系，是一个佛教法王和世俗领袖的被施和施主的互利关系。这些都说明，西藏不是中国自古以来不可分割的一部分，不是这样的。 <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 北京当局要求达赖喇嘛承认“西藏是中国的一部分”。达赖喇嘛说，Yes，承认了；要求他承认“台湾也是中国的一部分”，按道理赖喇嘛是不适合对此表态的，台湾是不是中国的一部分怎么归达赖喇嘛说呢？他们整个就是逼迫人嘛，不讲理。但是达赖喇嘛为了能够和北京政府有个谈判的机会，能够和平解决西藏问题，还是让步了。在一个场合说了，台湾是中国的一部分。那现在北京又有第三个要求了，要你承认：“西藏自古以来就是中国的一部分”。达赖喇嘛说，这个我不能说呀！我是达赖喇嘛，我是上师，我是转世活佛，但我不能改变历史呀！所以他说“和尚的舌头不能说假话”。现在僵局就在这里。 <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 西藏自1950年始失去独立主权 <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 北明：那么也就是说这个西藏独立的历史是从1950年开始改变的。 <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 曹长青：是的。当时中共大军压境呵。共产党刚打败了国民党，士气正旺；另外它的军队太多了！当时解放军的兵力，超过了整个西藏的人口，这个仗怎么打呀？你全部人口还没人家军队多呢！再加上藏人多是佛教徒，佛教强调不杀生，哪有发展军队呀？怎么打呀？没法打的！ <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 北明：他们当时只有七千人的军队。 <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 曹长青：是的，同时共产党又软硬兼施，来了个虚假的方式，跟你签《十七条协议》，什么“和平进藏”呀、“达赖喇嘛、班禅喇嘛的政教领袖地位不变”呀，“不进行社会主义改造”、“一切照旧”、“不拿藏人一针一线”等等。好，等解放军进去了拉萨，就开始给你社会主义改造。拆毁寺庙，让你尼姑还俗，整个做这些，逼得藏人最后起义造反、抗议，结果被严酷镇压。 <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 所以我说整个北京的宣称，既不符合历史真实，也不符合逻辑常识。我们今天看，全世界有哪个国家在宣称，这里那里是我们自古以来就不可分割的一部分？谁是谁自古以来不可分割的一部分呀？这个世界从来都是分分合合的，尤其是中国的历史！什么南北朝，唐、宋、元、明、清，多少朝代，都数不清了。谁是谁历史上不可分割的一部分？这种“自古以来不可分割”，这八个字本身就是一个帝国主义的想法，一个霸权的说法。但是它就可以这么说，为什么？今天无论是新疆问题，还是西藏问题，主要是中国没有一个讲理的地方。共产党他不讲理呀，他垄断所有的报纸、电视、电台、杂志。 <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 西藏在民国时期完全自治 <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 北明：曹长青先生，还有一个问题我要问你，跟这个有关系。就是，如果说中国大陆的中央政府是集权的统治，它垄断了所有的言论，非要说西藏就属于中国。那为什么国民党政府，反对共产主义，信奉三民主义，为什么它也这么说呢？这里面还有什么其它的原因吗？请你分析一下。 <br />
<br />
<br />
 曹长青：但是现在国民党政府的政策越来越改变呵。当年在蒋介石的时候就有一个讲话，当年蒋退到台湾以后，说西藏到底怎么样，是选择独立，还是自治，由西藏人民自己决定。他有过这么一个讲法。 <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp; 北明：可是他当时也入侵过西藏。 <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;曹长青：那是清朝末期，清军攻进了拉萨，不久辛亥革命爆发，第十三世达赖喇嘛领导藏人，把清军全部赶了出去，再次重申西藏是个独立的国家。国民党军队没有入侵过西藏。国民政府时期，西藏基本是“事实独立”，蒋介石政府对外说西藏属于中国，但藏人完全自治，当时国民政府驻藏人数，都规定不可超过一百人。 <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;当然，国民党也有一个逻辑问题。如果你说西藏不可分割，那么你怎么允许外蒙分割了呢？那蒙古是多大一片土地呵。而当时蒋介石政府允许外蒙古人民进行公决、公投的。为什么允许呢？有苏联在背后，苏联支持，当时国民党政府惹不起苏联嘛。那后来共产党取代了蒋介石政权，建立了中华人民共和国，你怎么不跟苏联要回外蒙呢？你怎么不说那个公投是无效的，是你们苏联在背后支持造成的，怎么不敢了呢？他是强权政治嘛！当俄罗斯比你强大，你不敢了。现在全部的问题是只要强权，我惹不起，所以承认了。北京当局现在怎么不说外蒙是中国历来不可分割的一部分呢？而历史上外蒙确实是和内蒙一体的。迟到二十年代才分割出去的。<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;北明：西藏在历史上作为一个独立的国家有没有一个独立的标准呢？ <br />
&nbsp; &nbsp;<br />
&nbsp; &nbsp;曹长青：当然有！人家有国号叫Tibet，中国人翻译成西藏了，西藏人自己叫吐蕃，图博；人家有自己的国旗，雪山狮子旗；有自己的军队；自己的元首达赖喇嘛的产生方式；有自己的首都拉萨；有自己的货币，自己的税收；更重要的是，有自己的语言、文化和历史。不久前台湾有个展览，展出一些当年西藏人出国的护照，有其它国家盖的签证什么的。符合一个国家的这些条件人家都有呵。所以今天我们看西藏的历史，西藏是不是自古以来就是中国的一部分，事实在这明摆着的。尤其历史这个事情是谁也动不了的。不论你是列宁、斯大林、毛泽东，多少有强权的人都改变不了历史。历史是已经发生过的，谁也不可能改变的！ <br />
&nbsp; &nbsp;<br />
