“把长矛扔进大海” “让黑人和白人成为兄弟，南非才能繁荣发展。”<br />
<br />
“当我走出囚室、迈过通往自由的监狱大门时，我已经清楚，自己若不能把悲痛与怨恨留在身后，那么我其实仍然身陷牢狱”<br />
<br />
这位曾经坐过白人种族主义者二十七年监狱的黑人领袖，这位南非历史上第一任黑人总统，这位让法国第一夫人为其生日破例登台献歌的人，这位让一个亚洲的黄色面孔为其写下一首《光辉岁月》的黑肤色老人，纳尔逊&#8226;罗利赫拉赫拉&#8226;曼德拉以他虚弱瘦小的身躯走出监狱大门那一刻，内心这样告诫自己。那一刻起，他没有了敌人。<br />
<br />
从监狱出来的曼德拉，充满尊严、克制和使人和解的精神。他以他那巨大的人格魅力呼吁黑人克制复仇的欲望；他已经彻底地反省了早期的暴力抗争和失败的婚姻。他获得了救赎。<br />
<br />
我对他的敬仰在于他饶恕过去曾经迫害过他的人所体现出来的宽容和恩典。二十七年漫长的牢狱给他的身心带来严重的伤害，但他立志绝不向过去的仇敌和曾经伤害逼迫过他给他巨大肉体痛苦的人进行报复。<br />
<br />
“把长矛扔进大海！”<br />
<br />
“看着年迈的曼德拉缓缓站起身来，恭敬地向3个曾关押他的看守致敬，在场的所有的来宾以至于整个世界都静下来了”。1994年在他的总统就职仪式上，曼德拉邀请曾经看守他的三位前狱方人员出席典礼，他说虽然他深感荣幸能接待这么多尊贵的客人，但他最高兴的是当初他被关在罗本岛监狱时，看守他的3名前狱方人员也能到场。并邀请他们站起身以便他能介绍给世界各国的政要，并向这三个人致敬。这一刻令全世界为之震撼和动容。曼德拉博大的胸襟和宽宏的精神，让南非那些残酷虐待了他27年的白人无地自容，也让所有到场的人肃然起敬。这一刻他再也没有了敌人。<br />
<br />
1962年8月，曼德拉因创建非国大军事组织“民族之矛”总司令而被捕入狱，南非政府以政治煽动和非法越境罪判处他５年监禁，当时他年仅43岁。1964年6月，他又被指控犯有以阴谋颠覆罪而改判为无期徒刑，从此开始了漫长的铁窗生涯，在狱中长达27个春秋，他备受迫害和折磨，但始终坚贞不屈。白人统治者把他关在荒凉的布满岩石到处都是海豹和蛇及其他动物的大西洋小岛罗本岛上27年。当时尽管曼德拉已经高龄，但是白人统治者依然像对待一般的年轻犯人一样对他进行残酷的虐待。在总集中营的一个“锌皮房”中，白天打石头，将采石场采的大石块碎成石料。有时从冰冷的海水里捞取海带，还做采石灰的工作。他每天早晨排队到采石场，然后被解开脚镣，下到一个很大的石灰石田地，用尖镐和铁锹挖掘石灰石。因为曼德拉是要犯，专门看守他的人就有3个。他们对他并不友好，总是寻找各种理由虐待他。 <br />
<br />
他说，感恩与宽容经常是源自痛苦与磨难的，必须以极大的毅力来训练。<br />
<br />
我想，如果换成平常人等的我们，能做到这点的将是大海捞针鲜有几个。我不知道曼德拉这样对待他的敌人，不，这样对待让他痛苦过深深伤害过自己的他们的人对面的人，是不是傻逼，是不是脑残；我不知道，我只知道曼德拉赢得了整个世界，赢得了自己的一生，并使之辉煌永垂青史。<br />
<br />
“别担心，放轻松，要快乐！”<br />
<br />
85岁的曼德拉依然精神矍铄，性格开朗。在这位尝过近30年牢狱之苦的老人心中，自由就是幸福。<br />
<br />
“从今往后，我的生活添加了两个重要内容，第一个是格拉萨，第二个是到莫桑比克吃大虾。”曼德拉与莫桑比克前总统遗孀相伴晚年。<br />
<br />
海子说：从明天起，做一个幸福的人，面朝大海，春暖花开……<br />
<br />
我知道曼德拉做到了“从今天起，做一个幸福的人。面朝大海，吃莫桑比克大虾”～～～<br />
<br />
