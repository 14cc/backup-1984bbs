																											<div id="postmessage_477605" class="t_msgfont">四<br />
<br />
他的一生就是一季严冬，而他就是那傲立严冬的“忍冬草”<br />
<br />
维基百科：忍冬草<br />
别名：银藤、鸳鸯藤、左缠藤、金银花&nbsp; &nbsp; 学名：Lonicera japonica&nbsp; &nbsp;科：忍冬科&nbsp; &nbsp;属：忍冬属<br />
主要产於黑龙江、内蒙古、宁夏、青海、新疆、海南、西藏，朝鲜半岛、日本亦有分布，性寒，味甘，略有酸味，具有清热、解毒、凉血、通经活络。<br />
<br />
最重要的是它看似脆弱，但它却能在严寒中绽放一片鲜红。在韩国，有一位老人，他历经生活的磨难，政治的迫害，但他却能在失败中寻找希望，战胜生命中严冬。<br />
<br />
他就是掌握韩国政坛近半个世纪的“三金”之一的韩国总统金大中。<br />
<br />
因为母亲难产而处于昏迷状态中降临到人间。他的父母做梦也不曾想到这个小生命竟会在他以后的一生中经历那么多的苦难，更没有想到劫后余生的他还能当上大韩民国的总统。<br />
<br />
有谁能够想到这位声誉卓著的总统，他的一生经历过如此漫长的苦难历程：5次遭遇逮捕；在狱中度过6年时光；5次面临死亡；被软禁流亡时间长达14年之久；还有将近40年的政治迫害。40年啊，整个生命的二分之一。年轻时弃商从政竞选国会议员，五战五败最后倾家荡产第一任妻子在贫病交加中去世。他非常清楚自己从那一刻起，他前面的道路就注定了是一条由荆棘来铺就，在这条艰难的道路上，软禁，流亡，酷刑，监视，对于金大中来说，一段时间内几乎成了生活的全部结局。<br />
<br />
1997年，一个73岁的高龄老人，退出政坛三年的他，突然一个回马枪重新杀入韩国政坛并且成功入住韩国总统府青瓦台。这一天他已经为之奋斗了四十四年。<br />
<br />
当选韩国总统后，有人预言他会对迫害过他的政敌实施政治迫害，但是，金大中在当选总统后的第二天，就在记者招待会上声明，决不搞政治报复，并希望在韩国这片土地上永远不要再上演政治报复的悲剧。<br />
<br />
在他的就职典礼上，出现了曾经和他政见不同的前总统金泳三，和曾经疯狂迫害过他的前总统全斗焕、卢泰愚。而且又是这位“韩国的曼德拉”，亲笔签署特赦了曾经对他实行迫害的这两位前总统。他创造了不搞政治报复的先例。<br />
<br />
有记者就请这三位出席就职典礼抱有怀疑的态度问他：这是您主动的吗？<br />
<br />
金大中：是的。<br />
<br />
这一刻，我想他绝对是没有敌人的。<br />
<br />
作为一个普通人，跟曼德拉一样，自己承认不是没有过报复的心理，之所以放弃唾手可得的快意恩仇是因为他们一致认为：仇恨的结果还是仇恨，暴力最后还是暴力，以暴力开始的最终还是将以暴力结束。<br />
<br />
金大中说：我虽然特赦了曾经对我实行迫害的两位总统，但是我把他们过去实施的腐败政治，转换为新的民主政治、透明的经济及良好的社会氛围。换句话说，虽然我没有对他们个人进行报复，但是我改善了过去腐败的政治制度。而且，我认为这一点比对个人实施报复更为重要。<br />
<br />
是的，这一点更重要。但同时很难获得喝着狼奶长大的拆拿国人所理解；更别说认同。<br />
<br />
相对某些所谓“智者”的言论来说，我不知道金大中是不是个“傻逼”，是不是个“脑残”；但是我绝对明白和曼德拉一样，他的人格他的智慧在拆拿国里鲜有几个能达到这个高度。<br />
<br />
因为在他们的内心深处是没有敌人的。这点我能理解。尽管我还做不到，也没实力做到。<br />
<br />
2009年08月18日，韩国前总统诺贝尔和平奖获得者金大中病逝。无论他们在执政期间的功与过都不足以在此作过多深入讨论。在动荡的韩国政坛中“三金时代”从此终结，他带走了整整一个时代。<br />
