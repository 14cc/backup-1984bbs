													<h2>也说“我没有敌人”</h2>
						
						
																											<div id="postmessage_477602" class="t_msgfont">一<br />
<br />
“上帝啊,原谅他们吧,他们不知道自己在做什么......”<br />
<br />
当罗马人用铁链绑住耶稣的手脚，把他押往刑场宣判；面对成群结队汹涌而来向自己扔着石块吐着口水的犹太人，面对自己被罗马人宣布绞刑后群起欢呼疯狂的犹太大众；耶稣扛着沉重的厚木十字架，蹒跚地走过耶路撒冷的街巷；当铁锤高高地举起一锤锤砸击在十字架上遍体伤痕血迹斑斑套上铁刺头罩的耶稣无声承受着死亡前最后的痛苦的时候。<br />
<br />
耶稣如是说。 <br />
<br />
宗教与种族，神明与人性交织的一部根据真实故事改编的电影《圣安娜奇迹》里；虔诚的村民在牧师的带领下跪下祈祷，当子弹穿透牧师头颅之前，牧师如是说……<br />
<br />
子弹毫无悬念地穿透了牧师的头颅，毫无悬念地倾泄到全体村民身上，之后是逐个补枪，或者刺刀，总之无一幸免，除了一个被良心发现的德国兵放跑的8岁男孩……<br />
<br />
我不知道耶稣是不是个“脑残”，也不知道牧师是不是很“傻逼”，我只知道在苍蝇的眼里满世界都是垃圾。<br />
<br />
也许那条在《亵渎》里面的自称“忠诚的格里高利”“智慧的格里高利”“神圣的格里高利”的魔龙来到路边八卦小组，变成了崇尚光明的“格里高利”后，究竟是什么让它只学会了用脚思考而不再使用哪怕些微大脑细胞。（如果，我说如果，如果小组不是一个平台而是某个人，那么我会从这人的人品上考量为何产生了南橘北枳现象）<br />
<br />
圣经里说，要原谅一个人77个7次。要爱你们的仇人。去爱你的敌人。<br />
<br />
我只知道那种感觉应该是看着某个自己的血肉挚亲被折磨了2个小时后一种让人心痛的不能自己的感觉。<br />
<br />
我想，原谅一个人或一群人或一个团体或一个组织或一个集团，并不等于无原则地对他们好。伟大的圣经从来也没有教导我们毫无智慧的对别人无原则地好。同样，去爱你的仇敌，去爱你的对立面，去爱即将伤害和已经伤害过你的人，是叫我们成为一个爱的主动者，你不是因为别人爱你你才爱别人，你不是因为别人对你说了对不起你才原谅别人。反过来，你在别人还恨你的时候你就爱他，你在别人还不知道已经得罪甚至伤害你的时候，你就先原谅他了，你在别人竟然不能明白自己做了些什么的把自己给毁了的时候，你就去原谅他了。这样你就不是被动者而是主动者；我想这种伤害绝不不仅仅是肉体层面的。<br />
<br />
所以，基督徒的受苦，不是被逼，而是自己甘愿的。<br />
<br />
丹增嘉措是甘愿的；晓波也是甘愿的；我如是观。<br />
<br />
耶稣受审判时被打，他说：“我若行得不对，请你告诉我错在那里，我若行得对，为什么你打我?”这就是保罗所提醒我们的：不要凭着字句，因为字句叫人死，要凭着经意，因为经意叫人活。<br />
<br />
丹增嘉措、晓波们已经没有了敌人，我们呢？<br />
<br />
<br />
二<br />
<br />
“你以口才胜了我们，我必以刀剑胜你；你以无形空刀杀我们，我必以真刀实剑杀你。”<br />
<br />
那位以头颅做赌注而为真理辩论的提婆；那位经历三月之久让前来挑战的八方辩士全部认输的提婆；那位使数以万计的挑战者经过辩论而成为自己座下弟子的提婆；那位在在丛林静坐禅定思维了的提婆；面对以此言立誓被狂心所惑的外道弟子用冰凉匕首插进自己胸膛时，出人意料地非常安详笃定平和，手捂伤口对凶手说：“我这里有三件东西，袈裟、佛钵和盂盆，你赶快拿著逃命去吧，注意要走山间小路，不要走大道平川，否则肯定会被我的弟子们捉住。你还没有学到佛法，不知身名俱是人之所累，所以肯定会爱惜自己的身体寿命。……”<br />
<br />
我不知道提婆是不是个“傻逼”，是不是个“弱智”；我也不知道他的举动是不是一种说法而已；我还不知道他是不是仅仅表现出宽广胸襟而他的内心压根就没这么想；我更不知道他是不是秉承这种理念来化解仇恨。但是，我至少明白，他真的没有敌人，这种认知深入骨髓而成为他人格的一部分。<br />
<br />
与此同时，尽管我秉承“向来不以恶意揣测国人”的理念，但却从不去揣测他的“没有敌人”是不是存于“私敌”层面。<br />
<br />
是的，“有没有敌人不只是你自己说了算，你即使认为你没有敌人，谁也不是你的敌人，依然会有人侵犯你，损害你，与你为敌，全然不顾你眼里是否有敌人”，面对那些伤害你的人，面对那些强奸犯杀人犯恶警恶吏恶官僚，我们内心的慈悲是很何等的渺小而无助啊。因为慈悲的力量只能赋予给领悟的人。领悟不到的只是一群极端可怜的人；对于可怜的人你如何去恨？那些可怜的人连自己在做些什么都不知道，你又如何仇恨他们？我想，一个具备大爱的人怜悯都还来不及何谈仇恨。<br />
<br />
越是慈悲越是充满怜悯与关爱。只有恶毒愚蠢的人的内心才会成反比以真理之名行不义之事。<br />
<br />
苦难困顿伤悲并不因为你的慈悲而终止，但绝对可以因为你的爱而趋善。因为人性本质是去恶趋善的。佛教本义就宣示过“是人皆有佛性，皆具无上慧根”。与传统儒家相反，宗教的力量来源于人性本恶与身俱来的原罪。<br />
<br />
当这种宽容这种爱存放到到政治的层面，也就有了社会矛盾和解的解决方案。</div>

							
							
							
							
