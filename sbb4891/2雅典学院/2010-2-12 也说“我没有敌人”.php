<br />
我想，他们也许早就已经没有了原发的动机，他们已经心身践行合一，他们已经把这种慈悲的爱的殉难当成自身生活的一部分。如同一个苦行僧，为生命获得救赎作践肉体的痛苦。<br />
<br />
其实，我清楚的明白也能清楚地感受到他们更大的痛苦在于内心，在于内心的对生命的焦虑，对唤醒生命意志的巨大苦痛。<br />
<br />
丹增嘉措在这样做，晓波也是在这样做。还有很多的不知道名字的人也在这样做，他们默默践行忠贞不渝，坚韧、正值、宽容，充满尊严、克制、理性，还有具备着博爱的伟大情怀。<br />
<br />
