我时常在想，能说出“我没有敌人”这句话的人是个什么样的人，我对他们充满着好奇和与身俱来的敬重。我很难想象这些历经千难万苦的平常人等，面对那些严重伤害过自己的近乎禽兽的两脚动物们能有如此博大而深遽的思想内涵人格魅力。这思想的原动力来源于哪里？<br />
