													<h2>看得见的罢工，看不见的损失</h2>
						
						
																											<div id="postmessage_671500" class="t_msgfont">一<br />
先举两个例子：<br />
1、甲公司和张三（或若干人）签订劳动合同，期限3年，月薪1100（或任意数字）。后张三认为工资低，和甲公司协商未果。于是罢工要求增加50%的工资。<br />
<br />
2、甲公司和乙签订买卖合同，一个杯子卖10元。后甲公司认为杯子卖贱了，要求卖15元，否则拒绝交付。<br />
<br />
这两个案例中涉及到了两个合同，第一个是劳务（劳动）合同，第二个是买卖合同。这两个合同之间有本质区别吗？在我看来，它们其实都是买卖合同，只不过标的物不一样罢了。第一个案例是在货币和人的劳动之间进行交换，第二个案例是在货币和物（其实所谓的物，上面不也凝结了人的劳动吗？）之间交换。所以，莫之许老师所说的劳务合同不是通常的市场契约这个观点在我看来是不成立的（我生产出来杯子卖给你和我直接为你生产杯子有多少区别？而且，有哪一个契约能穷尽所有可能出现的细节呢？）。<br />
<br />
所以，如果我们认为第二个案例中的甲公司不能任意违约，拒绝交付杯子，那为什么可以认为第一个案例中的张三能够因为工资原因罢工呢？<br />
<br />
二<br />
退一步讲，即使我们认可劳动合同和普通市场契约不同，让我们看看罢工的结果吧。罢工创造价值吗？答案当然是否定的。财富不会因为罢工而凭空出现。所以在罢工成功后，甲公司多付给张三的那一部分工资，同样不是从天上掉下来的，而是从别的地方转移来的。张三多拿了钱，这是看得见的，但有人少了钱，这是看不见的。张三多拿的这部分钱，或者来源于企业家的利润，或者来源于你我这样的普通消费者。也就是说，张三工资涨了，但企业家及普通消费者的财富减少了。这么说吧，如果本田公司同意给罢工工人加薪，但以后你要买本田车肯定会付出更多的银子，你怎么考虑？<br />
<br />
因为一个工人认为他的收入不高，所以我就要把自己的钱给他。这和抢劫有什么区别？<br />
<br />
因为企业家的收入比一个工人高，所以就要把他的一部分收入给工人。这不就是毛主席的作风吗？<br />
<br />
三<br />
从本田公司工人的罢工情况来看，我看到的罢工原因好像不是因为工资的绝对数额低，而是相对低，即公司内部存在严重的分配不均。但由分配不均引起的正义感可以是罢工的正当原因吗？在什么情况下工人可以罢工呢？<br />
<br />
我个人认为，当企业不遵守法律（或者遵守了法律，但法律是“恶法”）和社会风俗，侵犯了劳动者的人身、财产权益，而政府（包括立法、行政、司法）袖手旁观的时候，工人们可以通过罢工这种极端的形式来维护自己应得的权益，比如安全的工作条件，比如适当的收入，比如对宗教信仰的尊重等等。但工人得到的，只是他们应该得到的，而不是所有他们想得到的。记住，别去美化工人，就像别去美化农民、资本家、小知识分子、警察、异议人士一样。和其他群体没什么区别，大伙儿都有私欲，都想不劳而获。</div>

							
							
							
							
