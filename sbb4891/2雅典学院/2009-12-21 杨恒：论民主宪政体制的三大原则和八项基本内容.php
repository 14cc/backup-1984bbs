																											<div id="postmessage_385221" class="t_msgfont">杨恒：论民主宪政体制的三大原则和八项基本内容<br />
 <br />
&nbsp; &nbsp; 民主宪政体制必须首先确立三大神圣原则，即“天赋人权”原则、“主权<br />
在民”原则、“权力分立与制衡”原则。这三大神圣原则共生共存，互为基础，<br />
缺一不可。只有同时完全体现了此三大民主原则的政治体制才能被称之为民主体制，<br />
否则均为专制体制或虚假的民主体制。这三大原则从理论与实践上讲，均包含一些<br />
具体的内容。<br />
&nbsp; &nbsp; 第一原则──“天赋人权”原则。<br />
&nbsp; &nbsp; 这一原则来自于“人人生而自由平等”为主体内容的天赋人权理论。天赋人权<br />
理论强调了人人生而自由平等，每一个人都平等地享有不可转让、不可剥夺的自然<br />
权利，其中包括生命权、自由权和追求幸福的权利。天赋人权的原则完全体现了自<br />
由平等的价值观念，从根本上否定一切人压迫人的不平等理论，诸如血统论、阶级<br />
论、种族论。天赋人权理论是文明社会的理论基础，其原则是文明社会的最根本的<br />
原则之一。<br />
&nbsp; &nbsp; 第二原则──“主权在民”原则。<br />
&nbsp; &nbsp; 这一原则来自于社会契约论中的国家理论和政府理论。国家之产生来自于自由<br />
人民的自由协议，因此国家的一切权力来源于人民的认可，人民是国家主权的所有<br />
者，国家的目的是为了保障和增进公民的福祉和人权；合法政府的产生是必须经过<br />
公民自由公正的投票选举，人民有权选举政府，同样有权监督和罢免政府。<br />
　“主权在民”的原则并非空洞的原则，它又包括两个具体的原则：<br />
&nbsp; &nbsp; 一、全民直选的原则。政府的产生应该也必须根据公民的自由选举结果而产生。<br />
任何拒不进行直接选举或利用间接选举中对程序的不公正设置，达到操纵选举和变<br />
相剥夺选民权利的行为都是完全违背“主权在民”原则的。以“人民”的名义行使<br />
权力，而同时剥夺人民的选举权的政府必然是非法的政府。<br />
&nbsp; &nbsp; 二、多数决定与尊重少数的原则。选举是以多数为结果的。多数原则虽为结果<br />
决定之准则，但同时尊重、保护、宽容持不同政见的少数群体的权利。少数意志可<br />
以经过辩论与合法程序逐渐成为多数意志；多数意志也可能经过合法程序变成少数<br />
意志。多数意志与少数意志存在相互转换的良性动态关系，应相互尊重相互宽容。<br />
多数意志不得以暴力和强权剥夺少数意志的一切合法权利；少数意志也不得通过暴<br />
力和强权剥夺多数意志的一切合法权利；这两种情况与“主权在民”原则相背离。<br />
&nbsp; &nbsp; 只有实行了自由公正的全民直选并同时实现了多数决定与尊重少数的原则，才<br />
能体现出“主权在民”的真谛。<br />
&nbsp; &nbsp; 第三原则──“权力分立与制衡”原则。<br />
&nbsp; &nbsp; 国家权力的分立制衡是对“天赋人权”原则和“主权在民”原则的真正有效的<br />
保障。众所周知，“绝对的权力绝对腐蚀人”。当权力不受制约没有分立时，即使<br />
权力的获得是通过全民直选的，这种权力最终也会由于没有分权与制衡机制而走向<br />
独裁进而丧失其合法的地位。人类社会的发展业已证明，任何没有分立制衡的权力<br />
必然会蜕化为统治集团维护其统治的专政工具。任何国家在未完成权力的分立制衡<br />
以前，我们是不能认定它已经是民主体制，权力分立制衡永远是民主宪政的基本原<br />
则和基本要素之一。<br />
　　由于人性的固有弱点，对于国家权力必须予以明确的分立与制衡，否则后果不<br />
堪设想。以民主选举取得政权的希特勒，打着民主旗号赢得胜利的毛，最终给本国<br />
人民和世界人民几乎都带来了“灭顶”的灾难，这种历史教训应深刻牢记！正如孟<br />
德斯鸠所说，“一切拥有权力的人们使用其权力一直到遇见界限的地方方肯休止。”<br />
“从事物的性质来说，要防止滥用权力，就必须以权力制约权力。”<br />
　　或许“天赋人权”原则和“主权在民”原则是人们容易接受的，甚至独裁者和<br />
专制集团也不敢公开反对，他们甚至要打着“人民”、“平等”的旗号去夺取政权<br />
进而进行专制统治。“权力分立制衡”是所有独裁者都异口同声反对和抵制的，既<br />
使迫不得以口头接受，也会利用种种非法手段将“权力分立制衡”的实质予以消除<br />
和架空。独裁者之所以对“权力分立制衡”如此恐惧，是因为权力分立制衡将会使<br />
“天赋人权”与“主权在民”的原则在国家政权体制的实际运作系统中得到坚强而<br />
有效的捍卫，从而使独裁者打着人民的旗号反人民的企图都会由于体制性的制约而<br />
破灭。只有将“权力分立制衡”原则作为民主体制的基本原则，民主才会坚如磬石，<br />
不可撼动。&nbsp; &nbsp;<br />
　　三大神圣原则支撑起民主宪政的基本理论构架．它们是人类社会最珍贵的<br />
文明；它们普遍适用于东西方任何国家，它们是自由文明社会的准则。任何一<br />
种理论，不管它以何种籍口来直接或间接地反对这三大民主原则，最终都无法<br />
掩饰那种理论本身的反动性，其实践结果必然是反人类、反自由、反民主的。<br />
 <br />
&nbsp; &nbsp;根据三大民主基本原则，导源出民主体制的基本内容，这些基本内容实际上也<br />
就是民主体制的基本要素。只有通过对这些基本内容的确立，民主体制才可能完全<br />
体现三大基本原则的真谛。这些基本内容包括以下八个方面：<br />
&nbsp; &nbsp; （一）宪法精神的确立。在宪法中从根本上确立“天赋人权”、“主权在民”<br />
“权力分立与制衡”三大原则，使之成为国家宪法精神的主体，从而确立民主体制<br />
的法律基础。<br />
&nbsp; &nbsp; （二）基本人权保障制度的确立。从法律上切实保障每一个人的基本人权和自<br />
由，诸如人身自由、言论自由、新闻出版自由、结社组党自由、游行示威自由、宗<br />
教信仰自由都应是公民可以充分享有的基本人权，并应该有相应的法律予以明确保<br />
障。废止一切侵害公民基本人权的各种法律制度。<br />
&nbsp; &nbsp; （三）自由公正的竞选制度的确立。人民的自由投票是对各党派、团体、个人<br />
争取管理国家权力的最终裁决。人民有权以投票的方式直接选举国家元首及国会议<br />
员。任何党派、个人只有经过自由公正的竞选才能进入国家政权，一切命运取决于<br />
选民的意志。任何试图以暴力的手段夺取政权和以国家强权维持政权的行为都是非<br />
法的。民主体制从根本上否定非法政权形式，自由公正的选举是获得管理国家权力<br />
的唯一合法途径。<br />
&nbsp; &nbsp; （四）两院议会制度和内阁制度的确立。实行以国会参、众两院为国家最高立<br />
