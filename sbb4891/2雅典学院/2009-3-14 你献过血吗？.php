还有人说：你说的我都知道，但是为了广大病患的幸福，我就要献。OK！你很伟大，我敬佩你。可是，在你没有无偿献血的时候，难道病人就无血可输了？当然不是 这样，本老在医院工作的时间可能比你的年纪还大，我得告诉你： 正是因为无偿献血者的热情使病人承担了更高的用血成本，因为血液的暴利，政府就垄断了采血的渠道，在没有血站的时候，每200mm血液价格是70元人民币，现在的价格是每200mm大约200元人民币。 <br />
