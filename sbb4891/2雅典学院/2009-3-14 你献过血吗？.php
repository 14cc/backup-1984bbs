最近，关于无偿献血无损于健康的谬论铺天盖地，作为一个普通的医务工作者，我认为我有义务对此作出合理的解释。 首先，什幺是血液？ 血液所含的三种血细胞分别是：红细胞、白细胞、血小板。 血液的每一种组成都有它的特殊的功能，血浆可携带水份、盐类、养分、荷尔蒙和药物到我们的组织，并带走代谢废物到肺脏及肾脏，经由呼吸及尿液将之排出身体 其中白细胞可帮助你身体抵抗感染，血小板的功能主要是止血，当你受伤时，他们会粘在一起并堵住血管的破洞，阻止血液进一步流失。 红细胞是血液中数量最多的细胞，它充满了红色的血红蛋白，所以血液看起来是红色的。众所周知，生存是离不开氧气的，人们吸入肺脏的氧气便和血红蛋白结合被 运往全身各个组织，以完成细胞的新陈代谢。红细胞的寿命只有120天，它们在脾脏内被破坏掉。所有的血细胞均在骨髓内由造血干细胞分化而成。 这就可以很清楚的看出来，血液对身体的重要程度，那幺为什幺献血对身体有害呢？ 因为献血会造成以上血液功能的弱化甚至丧失。简单的说：红细胞的减少会使身体各部分器官得不到足够的氧气，细胞的各项生命功能无法发挥，献血后产生的头晕现象就是因为脑部没有得到足够的氧气供应，和一氧化碳中毒时的现象是一个道理。 <br />
<br />
白细胞的减少会使身体抵抗病毒的能力下降，容易生病，献血后身体虚弱就是这个原因，免疫能力的下降就使本来无足轻重的感冒等病毒有可乘之机。 顺便告诉大家，著名的艾滋病的另一个名字就叫“获得性免疫缺陷综合症”，血小板的缺少会使伤口难以愈合，流血不止，当然，谁也不会倒霉到献完血就受伤。 再顺便告诉大家，白血病产生的最严重的后果之一就是血小板减少,,《血疑》年纪大一点的人应该都看过吧？有人就说了：你说的不对，大家都说献完血后身体会生产出足够的血液补充。刚才我说过“所有的血细胞均在骨髓内由造血干细胞分化而成。” 造血干细胞凭良心讲是有寿命的，也就是说：造血干细胞的造血机能不是无限制的。 有一个很简单的例证：大多数老年人的骨髓造血功能仅是年轻时的50%，由于造血机能减退，易出现贫血，而且不易恢复。所以造血干细胞我认为是消耗品，而且是不可恢复的！ 还有人说了：书上说，献血后2周，血液的容量就会补充完成。对，你说的对，但是请注意：补充的是什幺？据我所知，身体在2周内补充的应该是血浆，血浆和血液是2个概念哦。 血浆的成分包括：水,,91%,,、蛋白质,,7%,,、脂质,,1%,,、糖类,,0.1%,,、无机盐类,,0.9%,,、代谢产物。看到了没有，这里也有一个简单的例证：献血后会感到口渴，就是因为要补充血浆，血站会无偿提供糖水或牛奶也就是这个原因，明白了吧？ <br />
