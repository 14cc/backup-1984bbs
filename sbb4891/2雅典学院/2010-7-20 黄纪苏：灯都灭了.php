													<h2>黄纪苏：灯都灭了</h2>
						
						
																											<div id="postmessage_749979" class="t_msgfont">黄纪苏<br />
&nbsp; &nbsp; 来源：凤凰周刊2010年第19期 总第368期<br />
<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;去南方出差，晚上无聊打开电视，忘了是浙江还是江苏台，正在播一个男女相亲节目。我一般不大看电视，但只要看到这类节目一般都会看完。要了解世道人心的变化，最便捷最实惠的莫过看男的都想娶谁，尤其是女的都爱跟谁了。这类节目不一定可靠，但起码比社论党章可靠。直到2007年党章的头一句话都还是“工人阶级的先锋队”。其实即便是毛时代，女的找男的，工人也不是首选，更别说农民了。不过毛时代普通劳动者的社会地位，比今天还是高不少。上世纪50年代末60年代初之所以出现大饥荒而未出现大动乱，相对平等的社会关系应该是一个重要的条件吧。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;音乐一惊一乍间“幸福门”打开，跑进来一位三四十岁的农民工。面对一字排开、花枝招展的时尚美女，农民工除了胆子几乎什么都没有：没房，没车，没个头，没风度，没学历。“电大”学历他自然有：电视明星那种半小跑腿半大阅兵的台风台步，还有电视主持人一年四季也不惦记换换的贫瘠语言，他都学了且都没学到位。他虽然什么都没有，但还有自知之明，知道自己什么都没有。只是，他自认为拥有一项别人没有的长处：他愿意倒插门。倒插门在传统的父系血缘社会里算得上一种可以出让或交易的价值，但搁到老婆都能互换的当今、房子比什么都贵重的当下，这还算价值么？起码对于现场的美女来说，连屁都不值――屁股的屁：节目中某美女就要求另一男选手转身，说“我想看看你的臀部”。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;民工上场后，美女们脸色很难看，就好像雪白的婚纱被要饭的抓了一把。美女们说话很难听，她们“麻烦”农民工“活得现实一些”；“劳驾回到真实的世界”；“可以同情你，可怜你，但不会接受你”。还有一位生怕农民工听了不铭心刻骨，说她家有一座别墅，两部法拉利；她是研究生学历，可以嫁给两辆兰博基尼；她要找的是男友，不是“男佣”。这样近乎辱骂的“真情告白”跟这类社会痒痒肉上的轻佻节目，跟美女们日夜经营的脸蛋、总算加入的阶级、正在恶补的风度，似乎极不兼容但又异常匹配。中国的资产阶级（当然不是全部）刚刚抢得一身臭汗，格调修养只能俟乎孙儿孙女了，第二代就是用法国香水从腊月泡到来年立秋，也很难去掉那股子车匪电霸气。现场点评人万峰拍案大怒，说你拥有再多财富，哪怕你拥有半个地球都与我们无关！我也很愤怒，但愤怒归愤怒，美女只不过以打砸抢资本主义阶段所特有的傻大姐加胡汉三方式，道出了中国贫富分化的现实。在这个现实中，谁跟谁都是有关的。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;其实，这个现实让我感到的更是忧惧。贫富分化哪国都有，但像中国这样丧心病狂的则少有。这次在南方从苏州往上海的汽车上和一位日本经济学家邻座，问起他们国家的情况，老先生说日本经济多年来一直很差，好在贫富差距不大，社会还算稳定。而中国几十年改革走的是一味“拉大差距”的基本路线，财富像嗜血的蚊蚋在社会的伤口上团生麇集。我进过穷人的蜗居，有些跟废品收购站没两样，除了小。我也见过富人的豪宅，想形容一下词儿不够，得参考《阿房宫赋》。随着市场经济恶性膨胀为市场社会，财富价值观一家独大，笑贫不笑娼成为亿万人的内心法则。在同一个但非同一期节目里，有位女嘉宾便喊出“宁坐宝马车里哭，也不坐自行车后座上笑”这种八大胡同员工都未必喊得出的口号。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;由亿万劳动者出资、由各级政府掌控、由各路文化精英伙同各色男女流氓操办的大众传媒，都争先恐后地讴歌财富，肆无忌惮地嘲笑劳动，针扎锥刺着底层民众逼仄而灰暗的人生视界。我有回随便浏览了十个电视频道，发现其中有六个正在弘扬影视明星奢靡无耻的生活。文化的恶之花像黑衣女巫摇荡在道路两侧，欢送中华复兴的列车从阶级分化的丘陵地带直奔社会分裂的大峡谷。财富分配上一刀两断而财富价值观上一统江山的社会，是对底层人群的双重围猎，不但将其驱往社会经济的边缘，还要将其逼入心理文化的绝地。节目中农民工渴望发家致富而不能、奋力出人头地而不遂、只好以屈身为进身的入赘计划遭唾面自干的经历，就是这个广大现实的缩影。主持人最后请神色黯然的农民工退场，告诉他“灯都灭了”――没人要他，则更像是未来走势的不祥隐喻。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;如今在中国，如果谁为弱者说话，总会有意识形态医护人员跑来为他体检。混得怎么样是一项重要指标，混得不好说明他狗急跳墙，混得还好说明他别有用心。那我也自检一下：底层我也许还不算，但起码算是他们邻居，邻居失火，我会遭殃，因为除了中国我再无别处可去。我对未来无奢望，平常就行，平安就够。绝大多数中国人的愿望也都不过如此。但以中国社会日渐浓重的戾气，开始弥漫的杀气，我很怕这希望落空。中国几十年经济成就很难说不辉煌，但社会代价也很难说不惨重。华灯下崛起跃进的中国拖着一个长长的影子，那是追债索命的中国。被发展遗弃、被富裕羞辱、被未来谢绝的人群要真是牛马也就罢了，可惜他们不是。不是就不会放过你。你在他们那儿播种了什么，你还将在他们那儿收获什么。能量会转化，但不会乱转：你右手扇过去的一掌，有可能转成你左腿挨的一刀，也有可能转成你左右眼皮日夜不停的惊跳，但不大可能转成你心里的悠悠钟声、梦里的融融月色。从王斌余到邓玉娇，从通钢到富士康，络绎不绝的杀人和杀己者像惊叹号一样走上中国未来的地平线。他们不会只代表极少数精神病患者，而是代表了更广大的底层人群。这个人群如果老也上不了宴席，如果老是被东倒西歪的宾客叱骂嘲笑，那么有一天，量变会积累成一次地动山摇，让盛宴戛然而止。今年“维稳”预算达5140亿元，已逼近5321亿元的军费了。<br />
