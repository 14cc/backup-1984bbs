																											<div id="postmessage_414356" class="t_msgfont">规矩！――听新年音乐会有感<br />
刘松萝<br />
<br />
2009年12月31日晚，去人民大会堂听“中国人保之夜――德国德累斯顿爱乐乐团2010北京新年音乐会”。德累斯顿爱乐乐团有135年的历史，保持着典型的德奥风格。<br />
<br />
有意思的是，指挥是西班牙人拉菲尔&#8226;弗吕贝克&#8226;德&#8226;伯格斯先生。伯格斯是贵族后裔，按照法国和西班牙的习惯，“德&#8226;伯格斯”的意思就是“封地在伯格斯的人”。这位指挥德&#8226;伯格斯，就出生在祖先的封地上面。由于指挥的西班牙背景，音乐会的下半场演奏了西班牙音乐作品，包括比才的《卡门》第一和第二组曲。<br />
<br />
不和谐的是，整个音乐会都是乱哄哄的。从第一曲《红旗颂》开始，二楼的观众就上下左右地走来走去。这中间有人是迟到了，从下往上走；还有人大概是坐不住了，从上往下走。就这样，一直折腾到了终场。这情景，让人想起了斯特劳斯的一首圆舞曲，《无穷动》。<br />
<br />
还有一个男孩和一个女孩，一会从外面捏手捏脚地悄悄走进来，过一会又打着闹着往外狂奔，还不时放出咯咯的笑声。经过了好几个回合，才有一位女观众实在忍不住了，出去吓唬了一下。<br />
<br />
其实，有些观众来真的应该去听二人转。既然屈尊来听交响乐，起码要讲一点规矩吧。那些年轻的面孔，是不是告诉我们中国的文明还很遥远啊。我甚至有些诧异，有人要是在日后发达了，会不会在电视上向追星者们炫耀昔日听音乐会时个性张扬的壮举。<br />
<br />
不知道一楼是不是也那么乱。咋看起来，指挥先生还是全神贯注，兴致勃勃。<br />
<br />
最后一曲演奏完毕，观众开始珍惜来之不易，又即将逝去的时光，拼命地鼓掌。伯格斯加演了《北京喜讯到山寨》，大家一片欢腾。与小泽征尔先生不同，伯格斯的风格更加细腻，有些段落甚至很悠扬。加演勃拉姆斯的圆舞曲时，观众有节奏地跟着鼓掌。此时，伯格斯的拉丁性格发挥到了极致。好像不喜欢被观众左右，他在观众鼓掌的时候突然把乐队停下来，又在大家无法预料的时候轻轻地重新开始。<br />
<br />
最后，伯格斯先生指挥了《拉德斯基进行曲》。观众热情洋溢，跟着一起鼓掌。就在这个时候，不少人开始穿外衣，还有人提前退场了。真是抓住机遇啊，门票要找，要抢，退场也要未雨绸缪。要是做一个恶意的推测，是不是有人在终曲的时候好像得到了大赦令，罪好不容易受完了。<br />
<br />
你们着什么急啊，一年365天，不就是一场新年音乐会吗。再说，即使不是十年一遇，五年之内不会再听是很可能的。消停一晚上，老实一晚上，真的那么难吗？<br />
<br />
2009.1.5.零时</div>

							
							
							
							
