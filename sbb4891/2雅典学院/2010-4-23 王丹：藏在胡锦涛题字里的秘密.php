<img src="http://pic1984.12hp.de//up/1272027633.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
转载标签：题字书法杂谈 分类：谈天说地 <br />
<br />
&nbsp; &nbsp; 青海玉树地震，胡锦涛中断出访活动后来到灾区慰问。新闻报道中突出了一件事情，那就是他在孤儿学校的黑板上，拿起粉笔写了几个大字“新校园，会有的！新家园，会有的！”这块黑板据说已经被送到了西宁博物馆作为文物保存起来，因此，也就具有了分析价值。 <br />
&nbsp; &nbsp; 众所周知，书法是一个人的个性的表现。毛泽东为人霸气，一手的草书也是汪洋恣肆，的确是个人特色的写照。那么，以毛泽东的继承人自居的胡锦涛，他的书法让我们看到什么呢？<br />
<br />
&nbsp; &nbsp; 第一，我相信就是热爱胡总书记的人，也不能不承认，他那一笔字实在是有够难看。我们之前看过毛泽东的孙子毛新宇那著名的歪七扭八的书法作品，坦率讲，胡锦涛的书法只是略微比毛新宇工整一些，但是字体上显露出的幼稚却如出一辙。这表明，胡锦涛是一个对文化生活缺乏兴趣的人。否则，稍微有一些文化品味的人，也会对自己的笔迹多少进行一些修正的努力的。中国的国家领导人，喜欢到处题词，因此对于自己的书法相当在意也是一种传统。从毛泽东以下，一直到江泽民或者温家宝，题词的书法水平参差不齐，但是基本上都还看得过去。胡锦涛一贯遵循党内传统，但是书法如此之差，可见其政治之外的生活之干硬枯燥。而其书法，一笔一画，极其横平竖直，更可以看出其为人的贫乏僵硬。<br />
<br />
&nbsp; &nbsp; 第二，一般而言，国家领导人题词，事先是应当由秘书做一定准备的。但是从胡锦涛用粉笔来写的状况分析，这次题词应当是他心血来潮。然而，正是因为没有腹稿，题词的内容才能反映出一个人内心的思想线索。凡是经历过1950年代的人，都会感觉他的题词很耳熟，因为会让人联想到《列宁在1918》里面那句著名的台词：“面包会有的，牛奶也会有的”。这两句题词，除了明显地缺乏文采之外，可以明显地看出，在胡锦涛的思想意识深处，1950年代的苏联式教育的影响是根深蒂固的，以至于他在需要临时题词的时候，脑海中第一时间浮现的还是那个年代的叙事内容。胡锦涛曾经在回答“阅读过什么世界名著”的时候，提到前苏联的意识形态宣传作品《卓娅和舒拉的故事》，也可以作为以上分析的又一个佐证。<br />
<br />
&nbsp; &nbsp; 我们当然不会要求作为国家领导人，一定要文采飞扬，出口成章。前任总书记江泽民，倒是琴棋书画，样样都来，也只是落人笑柄而已。但是，从胡锦涛的书法，我们不难看出，贯穿他的思维的一条传承主线，显然还是中共老一套的意识形态，而他本人，又是拘泥型个性，这样的领导人，其治国风格就可想而知了。因此，从胡锦涛的书法看胡锦涛，我们可以得出一个结论，指望他扮演一个改变历史的人物，那就叫做：“缘木求鱼”。最后，我要送给胡锦涛我的山寨版题词，那就是“民主，会有的；自由，会有的”。<br />
