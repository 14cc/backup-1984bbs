													<h2>对于儒家批评者的一些回应</h2>
						
						
																											<div id="postmessage_413264" class="t_msgfont">感觉辩论起来太乱了，所以分成几点，一一和大家讨论。<br />
1.一个国家到底是制度重要还是文化重要？毫无疑问，是制度重要，制度塑造文化。文化无优劣，制度有高低，所以现在我们这个时代整天纠缠文化是没有多少意义的，而把中国的不幸归咎于文华，用朱学勤的话说：是把只允许讨论的问题当做必须讨论的问题。<br />
2.现代化就是西方化吗？没错，自由民主人权乃至于科学技术都是西方人发明的，可是问题就是，这些东西在西方是自始至终的存在吗？我可以准确的说，刨去牵强附会，这些东西在西方历史上根本就没有。也都是3r运动之后才缔造出来的。西方的中世纪不见得比我们的中世纪要好，所以我们要论的问题不是为什么我们的文化不如人家好（因为人家的基督教文化已经经历了现代的制度改造，成为了新的基督教文化）而是我们为什么没有能力改造我们的文化使之适用于现代化？毫无疑问，像鲁迅那样以救世主自居去改造国民性只能适得其反，唯一可以改造文化的，只有制度。49年以来，我们疯狂的改造文化，以至于发展到文化大革命的程度，可是文化有改造多少？78年以来单单经济体制的变革就不知道改变了多少我们头脑中的意识形态！<br />
3.中国古代是儒家还是法家？我敢说这里绝大多数反儒的人其实都不了解儒家，儒家追求的上古三代，是真正的封建制度，是一个伦理中心的封闭的社会体系，这虽然不是现代社会，但不要忘记，西方的现代化，就是建立在这个封建制度之上而发生的。对中国影响最大的无疑是法家思想，很多人都拿罢黜百家来攻击儒家，实际上皇帝一统思想这本身就是法家的东西，而儒家讲究天道，也就是皇帝之上还有更高级的法律，这类似于英国人说国王站外万万人之上，但必须站在上帝和法律之下，中国也是，皇帝站在万万人之上，但必须站在天和天道之下。事实上，抑制商人，户籍制度，强国弱民，抢劫富人，皇帝面前人人平等，类似文革的时候老毛面前人人平等，都平的是独裁者的奴才的思想都是商鞅的。所以中国古代是外儒内法。<br />
4.中国社会是停滞的吗？当然，是停滞的，可是不要忘记3r运动以前西方社会也停滞了上千年啊，而事实上西方中唯一自发过渡到现代社会的只有英国和气子国美国而已。法国德国都是后来跟进，而为什么英国可以演进到现代社会，我的观点，也是很多学者的观点，是因为运气！地理大发现带来了大笔的贵金属，引发旧有的贵族破产，商人们的地位上升，从而商人们之间的平等自愿原则逐渐影响了整个国家（可以对比的是西班牙，他在地理大发现中得到的贵金属要多得多，可西班牙却是整个西方世界最后自由的），而光荣革命的爆发式因为国王有了继承人，迎接公主和他荷兰丈夫的决议谨以65：45票数通过。所以我们没必要因为我们的祖先错过偶然因素而责备他们，我们要做的是，作为拥有后发优势的人，可以看清楚制度变迁的大方向，改造制度才是最重要的。<br />
5.为什么改造文化是个伪问题<br />
用改造文化去追求自由民主只可以适得其反，历史告诉我们，改造文化只可以用文化的方法――但这还能称之为改造文化吗？岂不是文化的自己发展了吗？历史上尝试改造文化的三个国家，法国，俄国还有中国遭到的只有厄运，具体可以参加朱学勤的《道德理想国的破灭》<br />
6.现代化，其实是个体的解放，个体的自由权利的强调（所以类似的后现代就是指对个体的限制），个体要走出小共同体（家族，封建领主）和大共同体（国家）两层限制，儒家倾向于小共同体，这是违反现代化的思想的，可是我们要清楚的是，对于中国，个体的自由的最大障碍是什么？大共同体还是小共同体，这不用我解释把？我们指责封建社会（其实秦以后就不是封建社会了）父母包办婚姻可是后来结婚却要向党支部批准，这不的不说是天大的讽刺，站在这个角度上说，我觉得鲁迅是傻逼<br />
7.西方靠什么现代化？<br />
西方中世纪对于个体最大的束缚是小共同体，这不用我解释，而西方的现代化过程，学过点历史的都知道是经历了民族国家的建立的，是个体利用大共同体摧毁小共同体，而成为自由的国家，那么我们中国，在一个历史上都是大共同体本位的社会里，我们是不是可以借用儒家对于大共同体的排斥来完成西方几百年前的道路呢？<br />
8.随便写了点东西，欢迎大家讨论</div>

							
							
							
							
