																											<div id="postmessage_608298" class="t_msgfont">WHAT IS A GREAT BOOK<br />
<br />
　　<br />
　　There is no end to the making of books. Nor does there seem to be any end to the making of lists of &quot;great books.&quot; There have always been more books than anyone could read. And as they have multiplied through the centuries, more and more blue-ribbon lists have had to be made. <br />
<br />
　　No matter how long your life, you will, at best, be able to read only a few books of all that have been written, and the few you do read should include the best. You can rejoice in the fact that the number of such is relatively small. <br />
<br />
　　The listing of the best books is as old as reading and writing. The teachers and librarians of ancient Alexandria did it. Quintilian did it for Roman education, selecting, as he said, both ancient and modern classics. In the Renaissance, such leaders of the revival of learning as Montaigne and Erasmus made lists of the books they read. <br />
<br />
　　It is to be expected that the selections will change with the times. Yet there is a surprising uniformity in the lists which represent the best choices of any period. In every age, the list makers include both ancient and modern books in their selections, and they always wonder whether the moderns are up to the great books of the past. <br />
<br />
　　What are the signs by which we may recognize a great book? The six I will mention may not be all there are, but they are the ones I’ve found most useful in explaining my choices over the years. <br />
<br />
　　Great books are probably the most widely read. They are not best sellers for a year or two. They are enduring best sellers. GONE WITH THE WIND has had relatively few readers compared to the plays of Shakespeare or DON QUIXOTE. It would be reasonable to estimate that Homer’s Iliad has been read by at least 25,000,000 people in the last 3000 years. <br />
<br />
　　A great book need not even be a best seller in its own day. It may take time for it to accumulate its ultimate audience. The astronomer Kepler, whose work on the planetary motions is now a classic, is reported to have said of his book that &quot;it may wait a century for a reader, as God has waited 6000 years for an observer.&quot; <br />
<br />
　　Great books are popular, not pedantic. They are not written by specialists about specialties for specialists. Whether they be philosophy or science, or history or poetry, they treat of human, not academic problems. They are written for men, not professors. To read a textbook for advanced students, you have to read an elementary textbook first. But the great books can be considered elementary in the sense that they treat the elements of any subject matter. They are not related to one another as a series of textbooks, graded in difficulty or in the technicality of the problems with which they deal. <br />
<br />
<br />
<br />
　　There is one kind of prior reading, however, which does help you to read a great book, and that is the other great books the author himself read. Let me illustrate this point by taking Euclid s elements of Geometry and Newton s Mathematical Principles of Natural Philosophy. Euclid requires no prior study of mathematics. His book is generally an introduction to geometry, and to basic arithmetic as well. The same cannot be said for Newton, because Newton uses mathematics in the solution of physical problems. His style shows how deeply he was influenced by Euclid s treatment of ration and proportions. His book is, therefore, not readily intelligible, even to scientists, unless Euclid has been read before. <br />
<br />
　　I am not saying that great scientific books can be read without effort. I am saying that if they are read in an historical order, the effort is rewarded. Just as Euclid illuminates Newton and Galileo, so they in turn help to make Einstein intelligible. The point applies to philosophical books as well. <br />
<br />
　　Great books are always contemporary. In contrast, the books we call &quot;contemporary&quot;, because they are currently popular, last only for a year or two, or ten at the most. You probably cannot recall the names of many earlier best sellers, and you probably would not be interested in reading them. But the great books are never outmoded by the movement of thought or the shifting winds of doctrine and opinion. <br />
<br />
　　People regard the &quot;classics&quot; as the great has-beens, the great books of other times. &quot;Our times are different,&quot; they say. On the contrary, the great books are not dusty remains for scholars to investigate, they are, rather, the most potent civilizing forces in the world today. <br />
<br />
　　The fundamental human problems remain the same in all ages. Anyone who reads the speeches of Demosthenes and the letters of Cicero, or the essays of Bacon and Montaigne, will find how constant is the preoccupation of men with happiness and justice, with virtue and truth and even with stability and change itself. We may accelerate the motions of life, but we cannot seem to change the routes that are available to its goals.</div>

							
							
							
							
