<br />
1、关于读书。<br />
<br />
书不在于读得多，读的杂，在于自己对于内容的区分和控制。人类文明史上的典籍多如牛毛，一一阅读既不可能也不必要。选取 内容，理解要义，和作者思考的逻辑结构，可能比单纯内容的记忆和强行吸收更有益处。因为这可以训练人的独立思维能力。这点在国内的学校是欠缺的，或者说是压制的。怎么做，从经验上来说，我以为应该这样：<br />
<br />
首先读一本书，要假定自己接受作者的所有观点。因为不这样，你就没法去理解内容。注意，是假定，而不是当真的接受。理解了内容和写作的缘由后，才能和作者引起共鸣，或者尽量明白其逻辑内容。<br />
<br />
任何一本书，往往重要的不是内容，而是作者选材，组织，描述和推理的过程。所以，记忆书的内容是国内脑残教学法最为可怕的恶果。结果造成了许多白痴式学者。</div>

							
							
							
							
