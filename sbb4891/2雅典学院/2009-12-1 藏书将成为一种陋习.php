													<h2>藏书将成为一种陋习</h2>
						
						
																											<div id="postmessage_350748" class="t_msgfont">南北<br />
<br />
读万言书，不如念一字诀。<br />
毫无疑问，我的这篇议论，假如被那些以藏有多少多少书为炫耀资本的人看到，将是一个不小的打击。<br />
藏书有公共藏书和私人藏书之分。前者大多以公共图书馆的形式出现，属于公共事业，不在我这里讨论的范围。我说的是后者，是分布在一些或富裕或贫寒或宽敞或狭窄家庭中的私人收藏。我们通常也会根据一个人有无藏书或藏书多寡，来判断这个人的学问和品位。<br />
公共藏书，是为社会大众服务的，自然是越多越好，读者越多越被视为成功。私人藏书却基本是为藏者个人服务的。那些已经成为学者、专家、作家、诗人或教授的人，或者想要成为这些人的人，无不对于藏书情有独衷。这首先是自己用着方便，就像人想要盖房子，必定要事先积累砖瓦木料，等积累到一定程度，料齐了，足了，再花费些人力技巧，房子也就成了。著书立说做学问，也大抵如此。<br />
但是，事情的发展变化，往往是出乎人们的想象力之外的。改变这样一种千年不变读书做学问方式的，是我们现在很多人都已经熟知，但依然算是新生事物的互联网络和手机。随着人们生存方式和学习方式的重大改变，特别是个人电脑、互联网和功能越来越丰富的手机的迅速普及，电子图书、数字图书馆的大量出现，以及手机版本电子书的涌现，私人大量收藏图书的习惯，渐渐就将成了一种陋习。<br />
藏书，且是一种自私和浪费，也是对于自然环境的一种间接侵害。因为书籍出版所必须的印刷及包装材料，纸张、油墨等等一系列物品，都是林木毁坏和环境污染的重要源头。更何况大量人力、运输等其他方面的投入，就更是资源消耗巨大。而电子图书基本不存在这样的问题。<br />
我这个观点，毫无疑问的对于那些以藏有多少多少书为炫耀的人来说，将是一个不小的打击。他们若是因此而愤怒，我也是可以理解的。<br />
不过，我这里似乎可以做一点说明，我不是简单的反对买书藏书，而是基于那些藏书而不读的人来说的，是基于将藏书当作高雅的包装而说的。<br />
那些死读书，读死书的人，虽不讨人喜欢，但毕竟也并不多么令人难以忍受。他们虽读而无用，但总是读了，总是有提高自身品味境界的愿望在里面。<br />
最令人讨厌的，就是将书用来装饰房子，摆出一副读书人或知识分子的架势来唬人蒙人。书到了这样人手里，简直就是被绑架，就是受侮辱就是被谋杀。非但失去任何本身的价值和意义，还被迫沾上了铜臭气世俗气，甚至有助纣为虐的嫌疑。<br />
即便是那些真正喜爱书的人，爱书成癖者，细察之下，也不难看出，也还是人的占有欲自私心在作怪。我更知道，所谓藏书者，大多都是男人，女人一般无此嗜好。女人更讲究实用，她们买书，一般不是为了收藏，而就是为了读，是一种真正的需要。她们更愿意去图书馆借书来读，不但节省了钱财支出，而且借了便读，读完就还，让别的人接着去读。<br />
特别是那些以读书人、文人或知识分子自许者，对于书就更有了很固执的念头。一是觉得家里不藏些书（不管看是不看），就无法彰显自己与众不同的身份地位，精神境界；二是在书店里看到自己喜欢的书，习惯性的就想要搬回去据为己有，担心此时不买以后便无缘再见。而就我自己的经验，一次买了一大堆书回家，除非因为研究、写作参考的需要，一般是堆在书桌上或放进书柜里，慢慢的也就淡忘了去。因为自认为是自家的东西了，何时读都行，也就在新的寻觅和购买中不再想起。所以，我们看到许多藏书人，书是很多了，而真正读过的，十不及一。能读而有得者，就更是百不及一了。<br />
更有那没钱买书的人，就到书店偷书，还自以为高雅。实际上偷书比其他的偷盗并不高尚，只是在“读书人偷书不算偷”的话语掩饰下，似乎偷的已不是书，而是知识或道德文章。这自然是文偷的一种自我辩解。好像书就成了文化、文明、知识或高雅等等这些好名声的代名词，一个人只要爱了书，就一俊遮百丑。我看这是一个天大的谬误。书这东西，既是美和文化知识的载体，也是恶和病毒传播的源头。因此，书就在不同人的眼里，有了好坏之分。<br />
我的看法是，在中国几千年的所谓文明史上，人们从书本里得到的灵魂营养，远没有受到的毒害多。因为，那些所谓的道德文章，基本是为统治者们服务的，是教导被统治者如何忍受，如何服从的。或者一些书本上的话语，一开始是独立的，清醒的，但出现不久便被那些掌握权力的利益集团所篡改，所利用。<br />
当然，另有一种说法是，藏书是为了做学问收集储存资料的需要，不得已而为之。这个我是有同感并且赞同的。自己手中有了相关的图书资料，在做某一方面的选题研究时，是会很方便很有底气的。甚至，若是占有了独家稀缺而他人没有的绝版孤本，就等于在某些领域占有了领先的优势，等于有了高呼舍我其谁的资格。<br />
为什么中国的读书人喜欢藏书？我看根本的问题，还是公共服务不到位，不能满足写作者或研究者对于阅读和参阅相关资料的需要。看来，这又要刨一下几千年来社会制度的老根了。这个问题，如果读一下国外的作家或学者传记，基本就能了然，包括马克思在内的思想理论家们，他们的研究，基本都是在公共图书馆进行的。支持他们论点的大量资料的获取，也基本是来自公共图书馆的藏书，而不是自己的书房。欧美的公共图书馆很多，且服务优良，完全开放，不是像我们这里，民国之前基本没有。到了民国时才学习西方开始建立，但一座大型的城市也最多一个区一座，中小城市基本就是一个城市一座。后来，到了二十世纪的后半叶，图书馆里的图书基本是按照当局者意识形态需要而挑选出来的。即便是数量和质量很低档的图书，对于一般的读者，也是很难借阅到的，要通过很多繁杂的审查手续。像国外那样随意的查阅获取资料，基本是没有可能的。并且，几十年来，独立的写作和思想研究，也基本在这块土地上被消灭了，这大概是马克思他老人家没有想象过的。如果他生在这样的地方，估计《资本论》是无论如何也写不出来的了。<br />
现在，虽然作为这块土地上的居民仍然受到诸多的限制，即便在互联网的使用上也不例外，但还是有了局部的光亮和改变。过去对于私人藏书和公共图书馆无可替代的依赖，也正随着网络和电子图书的普及而渐减。特别是功能强大的网络搜索引擎的出现，更是给人们带来了前所没有的方便和希望。你要找一样无论国内或国外的信息资料，只要不触及当局设立的敏感红线，在搜索引擎上输入一下，然后点击，基本都能得到满足。不用再翻箱倒柜的去找，或者费时费力的去跑图书馆了。<br />
恕我孤陋寡闻，不知道国外的学者、作家们，在互联网普及之前是否也有大量的私人藏书？若是他们也如我等这样，那我的这个问题还就有了一点国际性。但我前面说过，在欧美民主国家，制度保证了人们从公共图书馆获取信息的权利，想来，他们是没有这个必要的吧。<br />
当然，我关于藏书将成为一种陋习的立论根据，正是基于中国千多年来顽固的信息封锁制度，以及新兴的数码图书及手机电子书，终将使私人藏书这个延续上千年的国产陋习有了终结的可能。数码电子图书不但可以大量的节约人类的生存空间，而且还可以减少资源的浪费，好处多多。反正，我从直观上认为，藏书将越来越成为一种陋习，而陋习是需要改正的。<br />
我也有过藏书的陋习。上面说的那些藏书的自私理由，基本都是我自己有过的。<br />
后来我放弃了藏书，是因为我的生活发生了重大的改变。我选择了四处旅居的生活，选择了漂泊。每一次搬迁，我必须丢掉很多不必要的东西。我在长途的迁徙中，带在身上的书，不会超过20本。只有一些我认为与自己生命悠切相关的手稿、书信和日记，是不能丢弃的。但这并没有影响我的写作。第一，我过去读过的书，凡是有益的思想和知识，我已经将其融入了自己的血脉中，不可能因为几本书的丧失而丧失。第二，有了电脑和网络的帮助。凡是我需要的东西，基本上我都可以通过网络搜索到。并且，比书本更快捷方便。<br />
没错，电脑和网络，是对于人的一种解放。它让我们可以放弃原来很多传统的依赖和习惯。让我们更容易迁徙行走，更容易在选择和流动中获得自由。<br />
所以，对于那些故意给网络设置关卡障碍的人，我是发自内心的反对和诅咒，因为他们实际上成为了人类自由的共同敌人。（2009-1-18，昆明）</div>

							
							
							
							
