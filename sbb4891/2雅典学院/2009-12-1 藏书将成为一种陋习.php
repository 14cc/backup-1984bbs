													<h2>家无藏书</h2>
						
						
																											<div id="postmessage_352766" class="t_msgfont">??家无藏书<br />
　　<br />
　　在某大学“客串”期间，最羡慕的是X博导和Z“同仁”家中的满壁藏书。不过对一些人以“藏书”多寡和档次论“英雄”的癖好颇有反感，也对由此而生的互相攻讦（如“某某买书就像女人买衣服”、“某某书架上的书多数连勒口都没有裁开”）觉得好笑；于是有一次我非常直率的坦陈了自己“家无藏书”的实情。谁知一句“您老自谦了”竟令我差点当众冒出脑袋里一闪而过的“热昏的胡话”来。<br />
　　这话最后我还是私下对我的“同仁”说了：“爱因斯坦说过一句我想说而没有说出来的话――凡是书本上能查到的东西，我决不装在脑袋里；不过这句话还有下半截：凡是图书馆能找到的书，我决不藏在家中。”<br />
　　我深知这句话会得罪许多人，包括我那位“同仁”；我自己对他们的藏书艳羡不已的事实，也足以证明此语的言不由衷。而我的“家无藏书”，原因却是多方面的。<br />
　　小时候家里也曾经有些“藏书”，父亲的都是线装书较有“价值”，母亲的则多为《三剑客》、《黑奴吁天录》、《埃及三千年艳尸记》之类或精装或平装的“闲书”。在那个要命的“自然灾害”年代，父亲因脑溢血半身不遂被母亲接往江西医护；做了“临时孤儿”的我，全靠以“家藏”贴“肚皮”的办法渡“难关”。先是卖父母卖剩的“藏画”（“档次”最高止于一幅蓝瑛的“烟雨楼阁”，为此专程赴上海“荣宝斋”；其次为齐白石、吴昌硕和大量的任伯年），然后轮到一些红木、楠木家具，最后则是“藏书”。所有这些东西通通填了我的“高级肚皮”。<br />
　　东西卖光、“自然灾害”过去、父亲病故、母亲“支内”，理当“赤条条无牵挂”的我也（此其时矣！）进了劳改队。在劳改队一晃16年、“搬家”十余次，我都是“一根扁担挑两头”：一头铺盖卷，一头书。当然这些书就是由母亲按照我开列的书单寄来的真正属于“我自己”的东西了。<br />
　　说来也好笑，在很多人看来我是个可以说“嗜书如命”的人，但我自己对“书”的态度却只有“天晓得”：一是扉页、书眉等空白处可以密密麻麻写满文字；二是红蓝铅笔的条条杠杠、代替书签的摺角折痕很快就能使它愁眉苦脸、面目全非；三是越好的书“跑”得越快――我有个“好东西理当分享”的习惯，而越是“好东西”，“推荐”出去也就常常不知所“终”了。<br />
　　尽管如此，见到好书我还是要“买”，也深知一些“孤本”、“善本”的收藏价值（毕竟有父亲的基因在啊）。至今我还心系当年卖书的一幕：那已经是卖到最后卖我买来的书了。在苏州南门那家已经被我“门槛踏烂”的旧书店，在我那些再厚的书收购价也仅在几分到几角之间的“估价”流程中，一本薄薄的《野草》竟估出一元的“天价”来，而且那老头（诚实的人！）还停下来问了一声“阿好（行吗）？”吾虽年幼无知，本能也使我当时就明白自己从旧书摊上以两角钱购进的东西，决不可能无缘无故一下就“增值”于五倍；但毕竟稚嫩腼腆的我还是不好意思说出那句“不卖了”――于是今天躺在博物馆“收藏”柜里（当然不一定就是那本）、由鲁迅亲自设计封面的首版《野草》，就与我失之交臂。<br />
　　是的，要“收藏”就“收藏”这类有“收藏价值”的书――但这已经不是通常意义上的“藏书”概念了。至于我所理解的“藏书”，则是把书“藏”在“肚皮”里，就像当年那样――还是套用一下鲁迅的比喻吧：“青草”吃进“肚皮”已经变成“牛筋”、“牛肉”、“牛奶”了，如果在生命因饥饿受到威胁时还能用它来填饱现实的“肚皮”，不是件一举两得的大好事麽？<br />
　　所以我要说，能够“家有藏书”固然非常值得庆幸和珍惜，但如果“家无藏书”也真的无关紧要。我小时候家里的“藏书”就远不如图书馆里的对我更“受用”；而再“受用”的书也只不过是“书”而已，它永远不能与你“自己”相提并论。也许是长期的“逃命生涯”使然吧，我还“总结”出一句不怎么好听的“大实话”，干脆在这里把它不揣冒天下之大不韪说出来――<br />
　　其实“命”才是最重要的。“命”在就什么都有――包括书：“命”就是一本“书”。<br />
　　<br />
　　2004年6月18日星期五16时21分</div>

							
							
							
							
