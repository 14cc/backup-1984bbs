陈丹燕是上海著名女作家，从30岁以后起，陈丹燕就把所有海外旅行都用在对上海的探索上，尤其是上海人的身份和文化认同问题。她发现，从上世纪80年代起，对于外滩公园当年是否真的挂出过“华人与狗不得入内”这么一块牌子，历史学界已有不少人倾向于认为，这块牌子却是伪造的。陈丹燕说：“我想知道的是，如果这说法是不对的，那么对于这个从被殖民的民族出来的说法，殖民的民族是怎么想的。”为此，她首先去了伦敦，在图书馆里一点一点挖掘资料，然后是美国。<br />
她查过正规的历史出版物，甚至小学的历史书，发现没有人或历史照片能够明确证明，外滩公园曾经竖立过“华人与狗不得进入”的这样一块牌子，她仔细查看了当时管理外滩公园的殖民机构―――上海“公共租界工部局”的所有会议记录，试图弄清楚这个故事是如何流传出来的。<br />
她甚至找到了曾经出现在影视作品里的那块“华人与狗不得进入”牌子的制造者。“他是我一个很早就认识的人。他的师父曾在60年代造过这个牌子，当时是为了一个上海史的展览，而他是80年代造牌子的人。他告诉我，牌子其实是在一种玩笑的过程中造的，大家还在讨论，应该是从左到右还是从右到左写？如果是工部局造的牌子，应该是纯英文的还是中英文的，是英文在上面还是中文在上面？”<br />
一系列福尔摩斯探案似的访问，使得陈丹燕认为，“华人与狗不得进入”是一段伪造的历史：“它其实来自公园的游园规则，最早一条是‘这个公园是外侨社团专有，非外侨者不能进入’。后来由于发生狗咬伤孩子事件，公园又加了一条规定，‘狗和骑自行车的人也不能入内’，这些规则被人联系起来，概括成了‘华人与狗不准进入’。”不过，她认为，这段伪造的历史里有真实的感情因素在，因为当时上海的民族资本家已经很强烈地感觉到民族的不平等，觉得受伤，所以才伪造了这样的一句话。 <br />
（摘自2008年3月10日北京青年报《陈丹燕：被误读的不只是上海的历史》）<br />
<br />
陈丹燕的这篇访谈最近在新浪披露后，又一次引起了具有强烈义和团情结的极端民族主义情绪朋友们的众口痛斥，这些七八十年代出生的小青年，言之凿凿地，指天画地地发誓，当年的外滩公园门口，确实挂过这么一块辱华的牌子，他们还引用周作人（！?）当年写的文章进行佐证。<br />
但是，非常可笑的是，许多人对当年的历史一窍不通，信口开河，一个博友是这样写的“我不了解上海外滩公园是否悬挂过“华人与狗不得进入”的牌示，但上海黄浦公园的确悬挂过“犬与华人不准入”的七字金牌。”<br />
这位朋友胆子真大，但却无知到连常识都不具备的程度，竟然说外滩有2个公园，黄浦公园和外滩公园！ <br />
对这块著名的，经常被引证作为爱国主义教育素材的，虚拟的牌子――到现在人们连牌子的材质都没弄清，有说木牌的、还有说铜牌或铁牌的，周作人更有趣，竟然说是“金字招牌”――从这个角度看，牌子真的非常虚拟！<br />
而且，从未有人考证过，这块牌子何时开始挂在黄浦公园门口，又何时摘下，为什么摘下；华人何时不能与狗进入黄浦公园，又何时可以与狗一起进入――许多回忆录披露，在二三十年代，地下党经常在黄浦公园进行秘密接头联络，这说明，那时根本不存在华人不能进入黄浦公园的禁制，因为地下党都是华人组成的，如果真的“华人与狗不得入内”，地下党们只好跳到黄浦江里接头了！――这些情况，从另外一个角度证明，这块牌子绝对子虚乌有。<br />
我也做过深入考证，（这段史料，陈丹燕未见过，可作为她的文章的补充。下面这段文字，去年曾在我的博客谈到过，今天也算是旧闻新提罢）： <br />
中国的历史上从来没有公园，有的都是私家园林。在租界，英国人沿外滩建立了黄浦公园，当时是对全社会开放的，中国人一样可以进去游玩。但是，中国人至今未改的恶习；随地吐痰便溺、乱折花木，践踏草坪在黄浦公园频频发生。此外，外国人的宠物随地便溺，外国人在园中骑自行车横冲直撞，破坏了公园的环境，因此英租界巡捕房制定了如下规定：<br />
（1）脚踏车及犬不得入内；<br />
（2）婴儿车应在路边推行；<br />
（3）禁止摘花、捉鸟及损坏花草树木，凡儿童之父母及保姆等应格外留心，以免此等不规则事件发生；<br />
（4）不准进入舞台和乐池；<br />
（5）除西人之佣仆外，华人一概不准入内；<br />
（6）小孩无西人同伴，亦不准入内花园。<br />
从上面的规定我们可以知道，当时租界的出发点为了维护公园的环境。<br />
如果我们的先辈能够自尊自爱，有点社会公德，遵守一点公共秩序，不随地吐痰便溺不攀折花木，不在公众场合为所欲为――到现在这些恶习依然丝毫未改――这些规定（而不是挂在黄浦公园门口铜牌木牌铁牌）可能就不会出台。<br />
换言之，现在的中国人开的酒店剧院、高档餐馆，也经常可以看到“衣衫不整，谢绝入内”或禁止赤膊拖鞋者入内的告示呀。<br />
而且，该规定首先禁止骑脚踏车入内，而当时只有外国人才骑脚踏车。而且，西人的佣仆主要是中国人，同样可以进入。<br />
换个角度看，1900年前后，租界总人口64万，其中98％是中国人。如果真的实行种族歧视政策，租界就不可能让中国人进入并居住生活工作，形成华洋杂居的局面，象南非搞种族隔离区一样。<br />
许多强烈民族情感的学者，引经据典地强调某某人亲眼在黄浦公园外面看到“华人与狗不得入内”的铜牌或木牌，但始终找不到证据，哪怕是一张照片。80年代上海曾经为此时再次引发争论，经过找遍上海博物馆、档案馆、文史馆、方志馆，仍未找到实物或照片或文字档案。<br />
我也遍查史料，后来知道，始作俑者是20年代上海极端民族主义情绪的左翼文人，他们也是道听途说，以讹传讹。结果杜撰变成了历史。真实的出处，应该是英国巡捕房的上述六条规定，是有人包括周作人，故意把这六条规定“简化”为“华人与狗不得入内”的。<br />
我的史料出处是上海泰东书局1924年印行的由陈伯熙编著的《上海轶事大观》“公家花园”条目。<br />
陈是清末民初著名报人，辛亥革命后主编《中华新报》，以研究上海舆地史志知名，著述颇丰。<br />
他在“南京路命名之意”一文中，披露了鲜为人知的史实：“盖中英根据道光壬寅五口之约，世所称南京条约是也，筑埠之始，实以此路为起点，故以南京为名，盖纪实也。”<br />
南京路竟然是英国人当年为了庆贺中英南京条约而命名，这段史实，可能没有几个人了解吧？<br />
现在披露出来，愤青们是否会鼓噪把南京路更名呢？</div>

							
							
							
							
