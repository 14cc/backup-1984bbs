<br />
张骧云（1855～1925年）<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; <br />
张骧云，字世镳，号君扬，晚年又号冰壶，人称“张聋彭”，上海市人。世代习医，至张镳云已九世传人。设诊所于境内珊家园，后迁爱文义路（今北京西路）。因诊断准确，效果明显，名声渐盛。27岁时为某“烂喉痧”患者出诊，当舌诊时，病人突然呕吐，直喷其面。回寓即染重病，终至两耳失聪，赖“刺叭筒”助听应诊，因而有“张聋彭”之称。<br />
<br />
张骧云精研伤寒原理，对望神、验舌、切脉、按诊、察斑、辨汗尤为重视。临床讲究审诊求因，辩证论治。实践中能融会各家学说，变革创新，形成独特“张氏流派”，诊治伤寒热病，以“张聋彭一帖药”而蜚声江南。张氏诊病、一洗时医陋习，不以名医自居，不仅诊金低廉，而且不设“特号”，求诊者无论贵贱贫富，一律循序看病。即使名绅巨商，也绝不特殊照顾。遇有贫苦患者，免收医金，还资助药物。晚年于今凤阳路珊家园筑“淞补读庐”，杂种花木，静养写作。著有《君扬诊余随笔》文稿。<br />
<br />
清光绪三十三年，哈同于涌泉浜(今上海展览馆处)兴建爱俪园，张骧云五世祖茔正处园地之中，哈同自恃洋人势力，威胁利诱，强购墓地。他不畏权势，严词拒绝，抗争十余年，终获胜利。此事在上海民间广受称道。</div>

							
							
							
							
