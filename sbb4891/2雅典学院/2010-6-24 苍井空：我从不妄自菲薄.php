推特把她推向中国<br />
南方周末：你的推特都是你自己写的么，还是别人代笔？<br />
<br />
苍井空：都是我自己写的，包括从前的博客也都是自己写，不愿意别人帮我写。<br />
<br />
南方周末：大概什么时候开始注意到来自中国的留言？为何想到和他们互动？<br />
<br />
苍井空：4月初，我用推特才不久，关注人数到了2000，我就写下什么时候能够上一万的想法。后来看到有中国网友留言说，一万那不是很容易的事儿嘛，应该以十万为目标。看到那么多跟帖，我挺吃惊。我用推特这件事也没有在日本做什么宣传，还想着到一定时候再向大家宣告，结果就先在中国网友面前曝光了。他们有些写的是中文，所以我看不太懂，有些用英文写的，有些用不大通顺的日语写的，估计是翻译软件翻译的，我也全都看了。<br />
<br />
南方周末：后来你在博客上贴了一篇中文文章？<br />
<br />
苍井空：对，我觉得实在应该写点什么的时候，就写了一篇，然后让朋友翻译成中文。<br />
<br />
南方周末：你是什么时候得知玉树地震的？怎么想到给玉树捐款的？<br />
<br />
苍井空：那阵子电视上天天都在放玉树地震的消息，正好很多中国网友到我的推特来，感觉和他们特别近，就想着号召捐款吧。<br />
<br />
南方周末：最后募集到多少钱？通过什么渠道给到中国？以什么名义捐出去的？<br />
<br />
苍井空：10万多日元，以“苍井空”的名义捐到了日本红十字会，然后再转到中国红十字会。<br />
<br />
南方周末：以前你知道玉树吗？对中国又有哪些了解？<br />
<br />
苍井空：完全不知道玉树这个地方，因为中国太大了。我不太了解中国，印象中觉得中国面积很大，人口很多，都是在课本上学的一些外围知识。我去年秋天曾经去过一次上海，中国给我的印象就是中餐，川菜、大闸蟹啦，日本的中餐为了适应日本人的口味，可能不太正宗，但还是很好吃。我特别喜欢中餐，特别想吃正宗的中餐。<br />
<br />
南方周末：后来，这件事引起了日本国内媒体的关注，对你有什么影响吗？<br />
<br />
苍井空：报道很多，也有英语的报道，还有一些不知道什么语种的报道，看着写了“苍井空”三个字，估计就是写我这件事了……倒也没有什么影响，生活还是在继续，只是我的跟帖变多了而已。<br />
<br />
南方周末：中国很少有人通过正版看你的电影，你为什么还会和中国网友互动？<br />
<br />
苍井空：就算在日本，我也不是为了让更多人来看我的东西才跟别人交流，有粉丝说喜欢我，我就愿意跟大家交流。至于到底是正规渠道还是非正规渠道看我的作品，我都无所谓。<br />
<br />
南方周末：6月16日到中国代言游戏，是什么时候决定下来的？<br />
<br />
苍井空：本来已经在谈，后来推特这件事起到了推进作用。<br />
<br />
<br />
<br />
<br />
<br />
苍井空＝蓝色的天空<br />
<br />
南方周末：你原名叫做“jun”，那么汉字是写成“纯”，还是“顺”，还是别的字？<br />
<br />
苍井空：这个……保密。<br />
<br />
南方周末：苍井空这个名字怎么来的？<br />
<br />
苍井空：本来就特别喜欢蓝色，就想着用“蓝色的某某”之类三个字的名字。然后有天和我经纪人出去，在开车回来的路上，觉得天特别蓝，经纪人说：“蓝色的……天空”，“蓝色的”和“苍井”一个发音，日本也有不少人姓苍井的，名字叫做“空”的女孩也不少，嗯，就这么定下来了。<br />
<br />
南方周末：进这行的时候18岁？当时情形是怎么样？<br />
<br />
苍井空：是。就在涩谷，被经纪人发现，递给我一张宣传单，然后让我去当模特，我并没有马上应承下来。<br />
<br />
南方周末：当时因为男朋友反对，才没有去？<br />
<br />
苍井空：是的，他并不是反对AV这个事儿，而是反对自己女朋友去进入AV业。后来和他分了手，我还是很想去，于是给经纪人打了电话。<br />
<br />
南方周末：你在自传书中说你本来是个极其普通的女孩子，那为什么想要走这条路？<br />
<br />
苍井空：本来我就很有兴趣。不是说对性或者对当AV女优有兴趣，而是很想当一个“面儿”上的人。我倒没想过到底是通过AV还是女演员的方式，我觉得都无所谓。我进入这个领域之后，发现身边的确有不少人是因为借债或者父母的问题干上这行的。可我就是很普通的一个人，我的确不像其他AV女优那样有阴影。<br />
<br />
南方周末：当时你已经考下了保育员资格证，为什么放弃了？<br />
<br />
苍井空：虽然喜欢小孩，但不合适我。那是个女人世界，我实在没有自信干下去，也没有前途。再说也不是考上了资格就得干这个。<br />
<br />
南方周末：父母是如何得知你拍AV的事？还记得父亲当时的态度么？母亲为什么会说“不赞成，但支持”的话？<br />
<br />
苍井空：朋友到我家去说给了我母亲。不过结果非常好。实际上我从没跟父亲好好提过这个事儿，都是通过母亲转达。女性之间总是容易沟通一些，所以妈妈也比较支持我。<br />
<br />
人生关键词：持续力<br />
