													<h2>苍井空：我从不妄自菲薄</h2>
						
						
																											<div id="postmessage_701126" class="t_msgfont">6月14日，东京迎来了阴雨绵绵的梅雨季节。雨伞根本挡不住斜斜密密的细雨，南方周末记者带着摄影师找到了位于涩谷的Prime Agency经纪公司，小坐十分钟，苍井空来了。<br />
<br />
5月初，苍井空为中国玉树地震义卖募捐的消息传出后，南方周末记者就设法与其经纪公司取得联系，经过几番交流，终于达成了专访的意向。<br />
<br />
在日本众多AV女优中，苍井空是较有名气的一位，但绝不是最有名的。她出生于东京的一个普通家庭，有两个哥哥、一个姐姐和一个妹妹。从小就因长相清纯颇受众人喜爱，却不是令人头疼的问题少女，相反，她总给人温顺的感觉。高中毕业后，苍井空进入大专学习保育员专业，2003年考取保育员国家资格。其后不久，刚满18岁的苍井空在涩谷街头被星探发现，权衡再三后进入AV界发展。她身高仅155cm，但以外形赢得了众多影迷。<br />
<br />
一眼看上去，苍井空是极其“普通”的漂亮女孩子，淡淡的妆容，甜美的微笑，甚至连最基本的假睫毛都没有粘，采访中她常常为一点点小趣事就爽朗大笑。相对容貌，她的声音有一点点莽，又不失妩媚。她回答问题时特别仔细地使用敬语，用词十分干净。“今天，是我出道8周年的日子，我自己都忘了，早晨打开博客，有网友留言，祝贺我，我才想起来。”采访临近结束时，苍井空告诉南方周末记者。<br />
<br />
苍井空正在努力学中文、韩语和英语，因为她觉得翻译软件经常词不达意，而她自称是个追求完美的人。一个小时的采访结束后，她跑到伞架上帮记者找伞，送记者到电梯口，电梯门即将关上的瞬间，苍井空90度鞠躬道别。<br />
<br />
