													<h2>为什么说精英治国是最坏的统治形式</h2>
						
						
																											<div id="postmessage_606497" class="t_msgfont">　　精英治国，如果是指在独裁统治或民主制度都很健全的情况下，自然形成的精英治国，当然没什么不好。但如果指的是没有独裁或民主做基础情况下的精英治国，就是最坏的统治形式之一。<br />
　　因为这种精英治国，非常类似于所谓的“吏治国”，也就是既无强大的皇权，也无科举或选举，所有统治者都从吏开始做起，从最底层选拔上来。“吏治国”从技术上看也是最好的统治方式，这样的统治者多么熟悉统治啊！但从人性上看，这就是最坏的，而且从事实上看也是如此，没有哪个国家能用这种方式统治而不严重腐败。原因很简单：因为这么升上来的人，只能是官场积弊的获益者，而不可能是别的；整个官场也会形成一个完全封闭的体系，毫无外部制衡。<br />
<br />
　　这就是技术性与人性的冲突。下面讨论为什么精英治国非常类似于“吏治国”。<br />
<br />
　　首先，在现代，用科举制作为精英统治的基础是不可能的，考公务员可以，但那个考的是吏，而不是官。<br />
　　更重要的是，除了资产阶级以及附属于资产阶级和市场的精英以外，其它精英阶层缺乏一种像市场机制、科举制或民主制那样的客观考核标准，他们基本上就是靠互相认可来取得认可的，这就给积弊留下了巨大的空间。<br />
<br />
　　当然，也许有人会说了：既然如此，那么，我们为什么不可以主要依靠资产阶级和依附于市场的精英呢？<br />
　　这个招数看起来是有效的。在历史上，资产阶级往往可以进行高效、廉洁的统治。但是，在全球化的现实面前情况就不一样了。现实是：资本和面对市场的精英阶层的国际流动性太大，根本没必要在乎祖国和人民，这些人一不小心就成了“持不同证件者”“外宾”，你能指望他们什么？指望他们发扬国际主义精神？<br />
<br />
　　下面讨论一个相关问题：为什么知识分子阶层道德堕落了？<br />
　　前面提到的制度因素（缺乏客观标准）是一贯存在的，所以还不能完全解释这种堕落。另一个原因是：这种堕落就跟业余体育变成职业体育以后的道德堕落是一个道理的；在过去，人们是因为兴趣才学习知识的，现在是为了赚大钱。</div>

							
							
							
							
