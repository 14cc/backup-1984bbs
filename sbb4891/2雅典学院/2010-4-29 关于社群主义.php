[编辑] 社群主义的历史发展<br />
在1980年代,社群主义是西方政治哲学的两大主流之一。其中有罗伯特・贝拉，查理斯・泰勒，迈克尔・桑德尔和迈克尔・瓦尔泽等人对古典自由主义理论过分个人主义化的倾向、雷根总统时代的美国政府以及柴契尔夫人时代的英国政府提出批判。 <br />
在1990年代,十分负责任的社群主义者，如阿米太・爱特热尼，菲力蒲・塞尔尼克和威廉・加尔森等人，不仅强调在个人权利问题上，责任与社会利益相平衡的重要性，而且还强调确保强大的社群不会压迫个人的需要。 <br />
社群主义之国家观<br />
社群主义认为，国家有干预和引导个人选择的责任，其次个人也有积极参与国家政治生活的义务。社群主义要求的是“强国家”，认为一个缺少公共利益的社会即使再有公正，也不是一个好社会；而只有国家所代表的公共政治生活才能促进公共利益的实现，所以为了国家和社群的利益甚至可以牺牲个人的利益。</div>

							
							
							
							
