古典自由主义（Classical liberalism）是一种支持个人先于国家存在的政治哲学，强调个人的权利、私有财产，并主张自由放任的经济政策[3]，认为政府存在的目的仅在于保护每个个体的自由。古典自由主义发源于17世纪和18世纪，也因此，它通常被视为由于工业革命和随后的资本主义体制而产生的一种意识形态。言论自由、信仰自由、思想自由、自我负责，和自由市场等概念最先也是由古典自由主义所提出，后来才陆续被其他政治意识形态所采纳的。古典自由主义反对当时绝大多数较早期的政治学说，例如君权神授说、世袭制度和国教制度，强调个人的自由、理性、正义和宽容[4]。美国革命和法国大革命都受到了古典自由主义的影响。<br />
<br />
古典自由主义认为一国的真正财富不是金银总量的增加，而是其国民创造的商品服务的数量。政府的干预经常是阻碍了经济的增长，因此主张将政府排除出经济领域，让经济生活自行其事，让追求自身利益的无数的个体理性的计算来引导经济生活的调整，即“谁会应当尽量从政府干预中摆脱出来，尽量的自由”，“管得最少的政府是最好的政府”。<br />
