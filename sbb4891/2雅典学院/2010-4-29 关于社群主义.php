													<h2>关于社群主义</h2>
						
						
																											<div id="postmessage_589936" class="t_msgfont">老马的那套没人信了以后，就另有人开始拿社群主义来忽悠人了<br />
转一篇徐友渔的文章，介绍社群主义的<br />
<a href="http://www.aisixiang.com/data/32860.html" target="_blank">http://www.aisixiang.com/data/32860.html</a><br />
<br />
西方政治哲学中“自由主义-社群主义”之争介绍到中国时产生了一些不准确的印象，本文力图依靠仔细阅读原始材料，特别是有关最新发展的材料来对“自由主义-社群主义”之争的内容和性质重作判断。本文认为社群主义并未使自由主义陷入困境，而二者的共同点，以及社群主义对自由主义基本理念的认可，对我们意义更为重大。本文还强调在引进西方学理时注意中国的历史和现实条件的重要性。 ( <a href="http://www.aisixiang.com" target="_blank">http://www.aisixiang.com</a> ) <br />
&nbsp; &nbsp; 当代西方政治哲学中“自由主义-社群主义”之争为我们理解、研究本次会议主题“正义、共同体与民主(Justice， Community and Democracy)”提供了思想框架和主要内容，对这场争论的各种观点和倾向做准确、全面的描述，对其性质做正确的判断，不论对研究西方学术理论，还是将其借鉴、运用于观察中国现实，都是必要的。 ( <a href="http://www.aisixiang.com" target="_blank">http://www.aisixiang.com</a> ) <br />
&nbsp; &nbsp; 大致从上世纪90年代下半叶开始，中国学术界重视并大量介绍 “自由主义-社群主义”之争。但令人遗憾的是，有关情况介绍到中国之后，在不少人心目中形成了一种不甚准确的印象，似乎当代西方政治哲学的基本情况和主要特征就是社群主义取代了自由主义，就像人们以前简单地理解历史发展而认为社会主义取代了资本主义一样。 ( <a href="http://www.aisixiang.com" target="_blank">http://www.aisixiang.com</a> ) <br />
&nbsp; &nbsp; 这与最初的引介者的工作有关。有人在了解情况很少、毫无深入研究的情况下就迫不及待地大谈自由主义遇到的“挑战”、所处的“困境”和“危机”。比如，汪晖断言：“当代自由主义没有能力在一个同质化和异质化相互交织的世界里提供普遍主义的权利理论。”“当代自由主义刚刚还沉浸在‘历史终结论’的兴奋之中，却迅速地感觉到了较之以往更为深刻和严重的危机。”① ( <a href="http://www.aisixiang.com" target="_blank">http://www.aisixiang.com</a> ) <br />
&nbsp; &nbsp; 我认为，考察社群主义者一开始对自由主义某些观点的批评以及争论的发展，并不能得出汪晖想使人相信的结论。我在此文中欲要证明的是，研究事态的新近发展，认真听取参加讨论的各方学者对这场讨论性质的评价，更能看出汪晖的论断缺乏根据。马克思曾说，人体解剖是猴体解剖的一把钥匙，对当前最新材料的研读和引证，可以帮助我们做出较为正确的判断。 ( <a href="http://www.aisixiang.com" target="_blank">http://www.aisixiang.com</a> )<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 内部争论还是外部对立 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 有人表达了这样的看法：社群主义并不是对自由主义的否定和取代，而是自由主义内部不同倾向的争论。比如，一位新社群主义者说：“社群主义者并不想取代自由主义，而是要保全它……社群主义者是这样的自由主义者，他们想在人们中间重建对于这些问题的常识：国家的目的是什么，其人民在历史中有什么共同的东西，他们应该争取达到什么目的。”② ( <a href="http://www.aisixiang.com" target="_blank">http://www.aisixiang.com</a> ) <br />
&nbsp; &nbsp; 尽管许多书籍和论文都包含了“自由主义和社群主义之争”的字样，而且人们似乎公认麦金泰尔(Alasdair MacIntyre)、桑德尔(Michael Sandel)、查尔斯・泰勒(Charles Taylor)和沃尔泽(Michael Walzer)是社群主义的重要代表，但明确持社群主义立场的丹尼尔・贝尔在其著作《社群主义及其批评者》中却引证上述四人的原话，说明他们并不认为自己属于社群主义阵营③。沃尔泽说，他的立场比一般流行于美国的社群主义观点要弱，更容易合并到自由主义的政治学中。他还说，自由主义是自我颠覆的学说，它确实需要社群主义周期性的矫正，但如果说自由主义不一致，或者它可以被某种前自由主义或反自由主义的社群取代，那么这样的矫正并不是特别有助益。社群主义对自由主义的矫正只能加强自由主义的价值，而不能起别的作用④。 ( <a href="http://www.aisixiang.com" target="_blank">http://www.aisixiang.com</a> ) <br />
&nbsp; &nbsp; 而持自由主义立场的阿伦・布坎南在回应社群主义的批评时说，当代几种最重要的自由主义理论已经包含了一种结构，能够与社群的价值相调适。他主张，对自由主义的最好捍卫就是把社群主义思想中最有价值的东西整合进来，这是可能的，因为自由主义理论具有这样的潜力⑤。 ( <a href="http://www.aisixiang.com" target="_blank">http://www.aisixiang.com</a> )<br />
&nbsp; &nbsp; 现在来看看一些重要的哲学家是如何具体分析所谓“自由主义-社群主义”之争的。 <br />
&nbsp; &nbsp; 在一篇题为“社群式自由主义”的论文中，作者斯泊瑞根斯声称，他深深地服膺自由主义的真实性，同时也深刻地同情社群主义的主张和关怀，人们普遍认为社群主义理论是对自由主义的批判性替代，他认为原因部分在于“自由主义-社群主义之争”一直表现出对自由主义的曲解，他要通过重新表述自由主义传统来使它复原和恢复名誉。他认为自由主义是一种复杂的规范性学说，信仰自由和贸易自由对于人的尊严有内在的价值，同时也有助于平等、繁荣、社会的稳定与和谐。像洛克、密尔、亚当・斯密等古典自由主义者，绝不赞成海盗式的、极端的个人主义，而是认为个人只是在完成义务的条件下，在衍生出相互依存和责任、道德秩序的有效限制和人类同情力量的情况下才享有自由。到了20世纪，古典自由主义的两大因素：自由和平等孰为第一的紧张日益明显，由此衍生出自由至上论和平等主义两大阵容。他认为双方都不太重视人类德行和社群目标的问题，两翼各执一端，但只拥有部分真理。他主张，可以认为博爱是自由社会的最高目标，这样，他所喜欢的那种自由主义也完全可以说成是社群主义。他希望社群主义者和其他人一样支持以下目标：代议制政府、通过同意取得合法性、法治、公民权利和公民自由，社群主义者和其他学说的支持者一样有权分享自由主义传统，他们应该把自己视为这个传统的改革者，而不是革命者或离异分子⑥。 ( <a href="http://www.aisixiang.com" target="_blank">http://www.aisixiang.com</a> ) <br />
&nbsp; &nbsp; 自我定位为社群主义者的大卫・米勒在其论文“社群主义：左、中、右”中说，谈论社群主义者和自由主义者的争论是误导人的，被称为社群主义者的政治思想家的立场、观点大不相同，与社群主义形成对比的不是自由主义，而是个人主义。那么人们为什么又爱说“自由主义-社群主义之争”呢?米勒认为，因为主流自由主义(这是方便的而不是准确的说法)和个人主义有天然的密切联系，但这并不是逻辑关系，因为持个人主义观点不是采纳自由主义政治学说的必要条件，也不是充分条件，这里可以想想霍布斯，他从个人主义前提得到的是权威主义的结论。当社群主义者批评自由主义时，它们所反对的大大超过了应该得到的结论，除非使人误入歧途扩大自由主义的含义，不然他们批评的就不是自由主义。米勒大致划分了三种社群主义：中间派或自由主义派，坚持个人价值、自主选择和文化、道德的多元性;右派，把无所不包的社群看成社会团结的来源和权威的来源，社群不是志愿结合，是权威机构、等级体系，不能脱离;左派，强调社群应平等形成，应积极地自决。米勒认为，在现实中，新加坡和亚洲一些地方像右派社群，北美像自由主义社群，很难想象左派社群在现实世界中会是什么样子⑦。 ( <a href="http://www.aisixiang.com" target="_blank">http://www.aisixiang.com</a> )<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 新自由主义和新社群主义 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 在上世纪90年代初，一大批对所谓“自由主义-社群主义”的对立和二分深表不满的哲学家站出来亮明自己的观点。他们中有的人自称为自由主义者或新自由主义者，有的人自称为社群主义者或新社群主义者，还有人，比如高尔斯顿(William A. Galston)，被一些人称为自由主义者，被另一些人称为社群主义者。其实，如何自称或被人称呼，对他们已经很不重要。 ( <a href="http://www.aisixiang.com" target="_blank">http://www.aisixiang.com</a> ) <br />
&nbsp; &nbsp; 1993年在华盛顿召开的美国政治学协会第89届年会上有一个关于新自由主义的专题小组讨论，后来论文汇编成书，名为《新自由主义：调和自由与社群》。论者大力发掘和弘扬新自由主义的思想资源，以求被忽视的自由主义传统恢复生命力，这既能让当代自由主义者和社群主义者理解他们共同的精神遗产，又能促进彼此的沟通和理解。 ( <a href="http://www.aisixiang.com" target="_blank">http://www.aisixiang.com</a> ) <br />
&nbsp; &nbsp; 在该书导论“新自由主义和自由主义-社群主义之争”中，编者指出，对“自由主义-社群主义”二分的不满有两个阶段和层次。最初是霍尔姆斯(Holmes)、穆恩(Moon)、斯泊瑞根斯等人不满意那种简单化的图景，但仅此而已。编者更重视的是第二个阶段和层次，人们看到了自由主义传统的丰富性。如果对自由主义思想宝库做充分的挖掘，不但可以看到所谓“自由主义-社群主义”冲突是出于误解，而且连这种说法都不对：自由主义因为接受了社群主义的批评而对自己的立场做了调整，从而更有适应性。之所以不对，是因为自由主义本来就有那些东西，不是调整和吸收而来⑧。 ( <a href="http://www.aisixiang.com" target="_blank">http://www.aisixiang.com</a> ) <br />
&nbsp; &nbsp; 持相同或相似观点的并不限于《新自由主义》一书的编者和作者。比如，马塞多在其著作《自由之德：自由宪政中的公民性、美德和社群》中就从自由主义的理论传统和宪政民主的实践中阐述自由主义主动、公共的一面，回应社群主义者的下述批评：由于强调个人权利和消极自由，自由主义忽视对于公民性、德行和社群的正面的政治理念。作者在书中证明，自由主义可以提供一种有吸引力、有鲜明特色的关于社群和德行的理论，同时继续保持以自由为核心，以正义为优先。自由主义包含了正面回应社群主义批评的资源，因为它承认社会目标高于个人目标，权利结构和适当的法律体系高于个人的愿望与目的⑨。 ( <a href="http://www.aisixiang.com" target="_blank">http://www.aisixiang.com</a> ) <br />
&nbsp; &nbsp; 在比较集中、全面反映新社群主义思潮的著作《社群主义精读》中，编者阿米泰・依左尼在导论中说，新社群主义者所做的远远不只是传播已有的理念，老社群主义者强调社会力量、社群、社会关系的意义，而新社群主义者从一开始就关心社会力量和个人之间、社群和自主性之间、个人权利和社会责任之间的平衡。老社群主义者关心随着现代工业、城市力量的兴起社会的分裂破碎，关心社群和权威的失落，没有人探讨相反的危险：社群可能是压迫性、权威型的，可能侵犯到个人。这些90年代前的社群主义者不关心乔治・奥威尔在《1984》提出的反面乌托邦问题，左的和右的权威主义、宗教原教旨主义等问题也未进入他们的视野，而新社群主义者从一开始就认定需要同时应付两方面的危险。他们认为在社会秩序和个人自由之间有一个平衡点，在此范围内二者相互支持和加强，但若是一方单方面发挥得超过了平衡点，二者就会变成对抗性的，另一个平衡点在社会滑向无政府和过分的秩序之间。 ( <a href="http://www.aisixiang.com" target="_blank">http://www.aisixiang.com</a> ) <br />
&nbsp; &nbsp; 新社群主义者认为美好社会应该在国家和市场之间―――准确地说应该是在国家和市场之外建立，他们强调，以前的对立―――即一方支持市场，另一方支持国家干预―――是不够的，就像只关心个人自由和命令控制系统的对立一样，这会使一些重大问题，即社会、社会联系的重要性和道德的声音处于思考之外。新社群主义者强烈主张社会行为应该受社会联系的非正规网络和社群的道德之声来维持和指引。他们指出，如果由社群来对某些行为(如吸毒、超速行车、酗酒、婚内侵犯)做褒贬奖惩，会更加有效、更加人道和降低成本。因此，他们力主用说服而不是强迫的方式促进人们的有益于社会的行为。当然，他们并不认为社群自然就是具有美德之地，许多传统社群具有威权和压迫性，有人指出，三K党也是一个社群。人们最好同时是好几个社群的成员，多成员身份可以保护自己免受一个社群的过度压力⑩。 ( <a href="http://www.aisixiang.com" target="_blank">http://www.aisixiang.com</a> ) <br />
&nbsp; &nbsp; 塞尔茨尼克在此书的开篇论文“社群式自由主义的基础”中说：“当代社群主义运动有时被看成是反自由主义的……今天的社群主义者并不是反对自由主义的人，如果自由主义意味着强烈支持政治自由、社会公正、宪政权利、法治、充分的公民权、特别关心穷人和受压迫者的话。如果社群主义者批评特定的自由主义学说，这并不说明他们否定或不欣赏自由主义的主要理念和制度……我们是，或应该是‘社群式’自由主义者，或者――如果你更喜欢这么叫的话――自由式社群主义者。我们应该像杜威一样，把自由精神和对正义的追求与在实际社群中的负责任的参与活动结合起来。” {11} ( <a href="http://www.aisixiang.com" target="_blank">http://www.aisixiang.com</a> ) <br />
&nbsp; &nbsp; 阿米泰・依左尼在他主编的《新社群主义式思维》一书中说，我认为个人和社群的关系比仅仅是个人与集体的对立更为复杂微妙，个人和社群相互联系在一起，彼此支持和加强，任何抬高一方贬低另一方的做法都会损害到一些根本利益，这些利益产生于使这两种基本因素保持平衡。他主张，社群的价值观既不能由外部群体，也不能由内部少数或精英强加，而必须由社群成员的对话产生。世代相传的价值常常是出发点，但需要在环境变化和成员提出新问题时不断调整。社群价值只有在不与公认的核心价值矛盾时才具有合法性，决不能同意某些社群主义者的这种看法：只要社群在一些价值上有一致意见，它们就是道德上合适的最终标准。不论社群坚持什么价值，人的本性必须加以考虑，这是基本的规范条件。社群不能管得太宽，因为个人――不论他们多么受制于社会影响――有一些普遍的属性是不能由过分热心的社群或国家清除掉的{12}。 ( <a href="http://www.aisixiang.com" target="_blank">http://www.aisixiang.com</a> )<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 个人权利 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 在所谓的“自由主义-社群主义”之争中，个人权利是最重要的问题之一，其他很多问题都是由此而生。本文不拟全面讨论这些问题，还是分析和澄清由于误导性介绍而引起的混乱。 ( <a href="http://www.aisixiang.com" target="_blank">http://www.aisixiang.com</a> ) <br />
&nbsp; &nbsp; 汪晖曾说：“如果一个人真正地坚持个人的权利，并承认这种权利的社会性，他就应该抛弃那种原子论的个人概念，从而必然具有社会主义倾向。罗尔斯的著作是当代自由主义的经典作品，我们不妨去看一看，虽然我本人并不是罗尔斯主义者。” {13} ( <a href="http://www.aisixiang.com" target="_blank">http://www.aisixiang.com</a> ) <br />
&nbsp; &nbsp; 这里的问题典型地涉及到“自由主义―社群主义”之争，这关系到个人权利、原子论的个人概念和社会主义这三个范畴之间的关系。汪晖以罗尔斯为例想说明的是，真正的个人权利与原子论的个人概念矛盾，与社会主义一致;原子论的个人概念与社会主义矛盾。借澄清这里产生的重重混乱，我们也可以详细说明自由主义或社群主义的有关主张。 ( <a href="http://www.aisixiang.com" target="_blank">http://www.aisixiang.com</a> ) <br />
&nbsp; &nbsp; 首先分析与罗尔斯有关的问题。罗尔斯的最大贡献之一无疑是使契约论重新充满活力，他说：“《正义论》的目的是将传统的契约学说普遍化，并使之擢升到一种更抽象的层次。” {14}而汪晖最喜欢引证的查尔斯・泰勒在题为“原子论”的论文中说：“‘原子论’这个词用来不严格地表征17世纪兴起的社会契约论学说，以及那些后继的学说，它们可能没有使用社会契约的概念，但继续把社会看作是在某种意义上由个人为满足首先是个人的目的而构成……这个词也用于当代的学说，它们返还到社会契约论，或力图在某种含义上捍卫个人及其权利对于社会的优先性，或提出关于社会的纯工具性观点。” {15}看来，照查尔斯・泰勒的阐释，罗尔斯的个人概念是标准原子论式的。 ( <a href="http://www.aisixiang.com" target="_blank">http://www.aisixiang.com</a> )<br />
&nbsp; &nbsp; 至于说罗尔斯必然倾向社会主义，真不知能在他的哪本书中寻到端倪。 <br />
&nbsp; &nbsp; 如果只是望文生义或不动脑筋，确实容易说原子论的个人概念必然与社会主义不相容。但从学理上考虑则完全不是这样，因为我们首先要分清作为方法论或本体论的原子论个人主义和作为价值论的原子论个人主义。刚巧，又是汪晖最喜欢引证的查尔斯・泰勒对这个问题做了精彩分析。 ( <a href="http://www.aisixiang.com" target="_blank">http://www.aisixiang.com</a> ) <br />
&nbsp; &nbsp; 查尔斯・泰勒在《交叉目的：自由主义和社群主义之争》一文中(他做了说明，文章基本观念得自一位博士生未发表的论文)说，在争论中两类完全不同的问题很容易混淆起来。一类是本体论问题，说的是你认为什么是用以说明社会生活的要素，即你当作说明顺序中最后的东西，在这方面，原子论和整体论争论了好几百年，原子论常常指方法论的个人主义;另一类是倡导什么的问题，关系到一个人的道德立场和所采取的政策。在后一类问题上，人们有各种不同的主张，在一个极端，最重视个人的权利和自由，在另一端，则把社群生活和集体的善放在首位。采取某种本体论立场并不等于倡导某种生活价值，而这在争论中最容易发生混淆 。比如，坚持认为不存在原子式的社会，人们仍然需要在多大程度上的自由社会之间作选择。社会本体论的原子论并不等于生活价值中的个人主义，这一点，西方学者中有人产生混淆，中国学者的误解更严重。泰勒指出，原子论-整体论之争的每一方都可以和个人主义或集体主义问题的立场相结合，不仅有原子式的个人主义者(Nozick)和整体论的集体主义者(Marx)，而且有整体论的个人主义者(Humboldt)，甚至有原子论的集体主义者(B. F. Skinner)。在当今英语国家，有一大类影响极广的自由主义学说，比如罗尔斯、德沃金等的理论，人们将其称为“程序自由主义”，泰勒说，有一种思想混乱，有人批评程序自由主义包含了原子式的本体论，理由是这些理论谈到了个人生活计划，但它们完全可以是整体论，更有甚者，只有从整体论的角度才能更好地把握最接近这些理论的社会实践{16}。 ( <a href="http://www.aisixiang.com" target="_blank">http://www.aisixiang.com</a> )<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 　　高斯在其题为《包桑葵对经济个人主义的社群式辩护：政治理论复杂性的教训》的论文中也以包桑葵的思想为例说明这两对概念组合的复杂性。他说，包桑葵的社会形而上学是有机论的、社群主义的，但与此同时他强烈捍卫经济个人主义，而这种结合既是彻底的，又是自洽的。他说，自由主义者及其批评者两方面的人一直共同认为“集体的”“社群的”“有机的”社会形而上学是反自由主义的，但个人主义与自由主义，社群主义与社会主义即福利主义的关系要比人们说的更复杂。作这么一个流水出山的比喻也许是合适的：如无阻挡，它就顺流而下，但若筑了堤坝挖了沟，流水就会停止、反向而去。社会形而上学和政治理论的情况也是如此，如果出发点是集体的社会形而上学，很容易得到反自由主义的结论，但包桑葵采纳了这样的认识论：很难知道公意，公意会让个人知道他们的意志将表达社会意志，那么与个人主义相关的整个一套制度安排――市场、私有财产、政治的有限范围，对福利国家的批评――就会从完全是社群主义的世界观中出来。作者认为包桑葵向社群主义者提出了一个教训或挑战：如果认真对待整体主义研究方法，承认社会系统的复杂性，我们的知识有限，以及个人由其社会关系构成，那么像市场这样的“自动”调节就比国家计划的有意调节更是真正整体主义的。包桑葵令人信服地证明了，不论批评自由主义的社群主义者想达到什么目的，完全看不出他们何以摆脱他们最不喜欢的自由主义的这方面主张――基于市场秩序的私有财产{17}。 ( <a href="http://www.aisixiang.com" target="_blank">http://www.aisixiang.com</a> )<br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 差异政治与中国国情 <br />
&nbsp; &nbsp;&nbsp;&nbsp;<br />
&nbsp; &nbsp; 汪晖依靠全盘引证查尔斯・泰勒的“承认的政治”或“差异政治”的说法来证明自由主义遇到的“挑战”“危机”和所处的“困境”。他说：“自由主义把无视差异的普遍主义原则看作是非歧视性的，而差异政治则认为‘无视差异’的自由主义本身仅仅是某种特殊的文化的反映，因而它不过是一种冒充普遍主义的特殊主义。” {18} ( <a href="http://www.aisixiang.com" target="_blank">http://www.aisixiang.com</a> ) <br />
&nbsp; &nbsp; 首先我们要问，差异政治的理念是否已经成为公认的历史潮流，使得自由主义的个人权利和普遍的自由民主原则已成明日黄花?实际上显然不是。以美国一些大学对黑人采取降低入学考试成绩为例来看，这显然是一项引起争议的政策。对黑人的优待意味着对其他人的歧视，它的正义性遭到广泛质疑，尤其是对华人血统和其他亚裔学生，因为在为黑人降分时还加高了他们的入学门槛。甚至不少黑人也反对这项政策，因为照顾会使人产生依赖、不思进取，从长远看并不是好事。更重要的是，它反而使不少黑人学生产生自卑感，因为别人，甚至他们自己都容易觉得自己天生差一截，是靠照顾政策才能入学。 ( <a href="http://www.aisixiang.com" target="_blank">http://www.aisixiang.com</a> ) <br />
&nbsp; &nbsp; 汪晖对普遍主义原则的批判很容易使人联想起在中国上世纪50至70年代对“资产阶级”的民主和“法律面前人人平等”的批判。据正统说教，那种自由“不过是穷人有挨饿的自由，百万富翁有花天酒地的自由”。事实证明，尤其是中国、苏联的惨痛经验证明，以所谓“真正的”“实质性的”民主和平等取代程序性的、形式的民主和平等，得到的是灾难、浩劫。现在，西方社会中某些实验性的差异政治的政策是以个人权利和程序性正义有相当保障为前提的。 ( <a href="http://www.aisixiang.com" target="_blank">http://www.aisixiang.com</a> ) <br />
&nbsp; &nbsp; 汪晖的论证始终依靠引证查尔斯・泰勒的“承认的政治”一文，他没有引证收载此文的编者在导论中表述的重要观点：不持中立的自由民主允许公共机构促进特定的文化价值，但条件有三：一是公民基本权利得到保护;二是不能操纵接受那种文化;三是做出文化选择的机构应民主地负责任{19}。汪晖谈差异政治也联系了中国、苏联的实际，我想知道，针对中、苏历史经验这个背景，我们主要是应该为个人权利和普遍的自由民主原则呼喊，还是贬损它们，力图给正在争取它们的人们一种印象：这些东西已经失去价值，正在被另一些不同的东西取代。 ( <a href="http://www.aisixiang.com" target="_blank">http://www.aisixiang.com</a> ) <br />
&nbsp; &nbsp; 其实，在西方，真正的社群主义者是清醒的、严肃的。上世纪90年代刚开始，在美国有50多人(社会各界人士，大多数是大学教授)发表了一份题为“权利和责任”的社群主义宣言，其中有些话可以视为对中国某些社群主义鼓吹者的告诫。宣言并不把社群当成价值的最终来源和标准，相反，对非民主、非理性的社群价值高度警惕。它说： ( <a href="http://www.aisixiang.com" target="_blank">http://www.aisixiang.com</a> ) <br />
&nbsp; &nbsp; 有人想靠压制不同意见(以宗教、爱国主义和其他事业的名义)和书报检查把公民的或道德的价值强加于人，我们要对这些人说，你们的办法是无效、有害、在道德上站不住脚的……社群主义者并不一味说社群好，也不认为任何一类价值仅仅因为发端于社群就是好的。确实，某些社群(比如新纳粹)可能会培育应受谴责的价值。社群主义者认为――他们确实坚持这一点――社群的价值必须由外在的、普遍有效的标准，必须基于人类共同的经验来判断。 ( <a href="http://www.aisixiang.com" target="_blank">http://www.aisixiang.com</a> )<br />
&nbsp; &nbsp; 这份新社群主义宣言特别强调历史和现实条件的重要性： <br />
&nbsp; &nbsp; 社群主义的基本主张是在个人和群体之间，在权利和责任之间，在国家宪法、市场和市民社会之间寻求平衡，这是持久的事业。不过，由于这种寻求是在历史之中，在变化的社会条件中进行，评价什么是恰当的道德立场也将随时间、地点条件的变化而不同。如果我们是在今天的中国，我们会为更多的个人权利而作强有力的证明;而在当代美国，我们强调个人的和社会的责任{20}。 ( <a href="http://www.aisixiang.com" target="_blank">http://www.aisixiang.com</a> )<br />
