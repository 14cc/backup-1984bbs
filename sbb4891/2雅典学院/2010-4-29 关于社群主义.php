社群主义的出现，源自对1970年代罗尔斯《正义论》（A Theory of Justice）一书的背后自由主义理论的批判。社群主义全然反对自由主义的基本价值，认为自由主义忽略社群意识对个人认同、政治和共同文化传统的重要性，理论建构前后有其限制。依此，社群论者沈岱尔、麦金泰尔、泰勒和瓦瑟等人从不同角度批判自由主义的理论，并重新思考社群的意义，企图恢复濒临垂危边缘的社群意识。<br />
<br />
<br />
社群主义对于社群和群己关系的重视是承袭公民共和主义（civic republicanism）的传统而来，其可溯源自柏拉图《理想国》对话录，以及亚里斯多德对人类是社会性动物的强调。社群主义旨在恢复社群价值得重要性，而非想取代自由主义在当今社会中的重要地位，仅是希望修正自由主义对个人和社群的错误假设，并重申社群对个人的重要性。<br />
