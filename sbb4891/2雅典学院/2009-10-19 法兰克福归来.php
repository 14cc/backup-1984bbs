1、中国馆子与展会格格不入，人家都是来谈生意的，都是提前半年预约，几分钟就签一个合同。中国人是去吃盒饭的。三三两两，座谈聊天。<br />
<br />
2、开幕式上，近距离围观习近平和默克尔。一个德国女记者告诉我，她是来看扔鞋的，结果很失望。会场上火药味很浓，第一个上台发言的书商协会主席，在致辞结束后，恳求习主席给予中国作家和记者自由写作的权利。<br />
<br />
3、德国人也不鸟默克尔，说她什么都没有做错，是因为什么都没做（政治正确闹的），这话让我想起影帝。<br />
<br />
4、铁凝在主席台上摔了一跤。他的发言冗长而无趣，比副主席还长一半。可能过于紧张，下台的时候歪了个大趔趄，蹲在地上。<br />
<br />
5、书展主席博斯提到两个异议作家。州长考克提到艾未未。他们都呼吁自由。副主席很没面子。<br />
<br />
6、党棍的掌声。为了回击德方发出了自由“挑衅”，习很隐晦的回应了一句，大家还没反应更过来，座席左侧下方开始鼓掌欢呼，是出版总署组织的官员代表团。德国人没看懂，没有跟着鼓掌，所以偌大的和谐大厅。三次响起“部分”掌声。<br />
<br />
7、见到贝岭、戴晴、杨炼、高行健、龙应台……等等。贝岭是个活动家，四处“走穴”，为自己积攒政治资本。戴晴声色俱厉的接受采访。杨炼和高行健的对谈很精彩。龙应台的演讲很火爆。刘震云的访谈很精彩。<br />
<br />
8、莫言是最大受益者，在开幕式上讲话，是被官方推出的代表作家，他的书的翻译――一名汉学家，法大女教授，陪我去看了法兰克福对汉诺威的比赛，我们聊了很多，会出现在正式的报道里。<br />
<br />
9、诺贝尔得主穆勒去了大纪元馆，声援法轮功。热比亚去了中国馆。雪山狮子旗出现在中国展台，喇嘛在那里接受访谈。大纪元、独立中文笔会里，感觉都是一些妖孽。同行的几个人都有同感。<br />
<br />
10、我是受德国外交部邀请，与《中国农民调查》的两位作者、两位记者同行。一行五人，组团前往。住在美因茨，每天去法兰克福，因为法兰克福涌入了太多的人，街上随处可见中国人。据说官方组织了几千人。在去德国的飞机上，邻座是一位某省出版系统副厅级干部，我问你们有展台吗？答曰没有，问有会议、活动吗？答曰没有，我直接问，你是去捧个人场的吗？答曰是。一个人场3.9万元。<br />
<br />
11、我经常参加一些港台或者异议作家的活动，所以有公安部某二级警监给了我一张名片。还邀请我去参加中国文学之夜。我没去。<br />
