<br />
神化天安门运动是我们的心魔 ――我为何在《天安门》制作人的呼吁书上签名<br />
<br />
&nbsp; &nbsp; 作者：王力雄<br />
<br />
&nbsp; &nbsp; 最近，拍摄了纪录片《天安门》的美国长弓纪录片制作组（下称“长弓”）发出呼吁（见附件），谈及他们正遭受一场可能被<br />
<br />
耗尽资财而倒闭的诉讼。提起诉讼的是一家有财力的美国公司，公司总裁正是1989年天安门运动的学生领袖柴玲。长弓呼吁书从维<br />
<br />
护学术自由和言论自由角度，希望得到签名支持。<br />
<br />
&nbsp; &nbsp; 我签了名。<br />
<br />
&nbsp; &nbsp; 我之所以签名，不在于我能断定诉讼的具体是非，我对美国法律不懂，对被立案的商标侵权更是外行。我签名是出于这样一种<br />
<br />
看法――必须允许对天安门运动的反省。<br />
<br />
&nbsp; &nbsp; 对《天安门》影片如何评价，可以见仁见智。但是需要看到它是在进行反省，而且是迄今对那场运动的主要反省。我们应该支<br />
<br />
持这种反省，而不是站到反省的对面。这里所说的“我们”，是指站在天安门运动一方谴责当局镇压的所有人，因此这个“我们”<br />
<br />
应该既包括柴玲，也包括《天安门》的制作人。<br />
<br />
&nbsp; &nbsp; 我们对天安门运动的态度，长期局限在两个方面，一是赞美运动，二是谴责当局。赞美和谴责都是应该的，但不能是唯一的，<br />
<br />
甚至不是最主要的。专制权力不会因为谴责而改变，这我们早已看到。而停留于赞誉，对我们又有多大意义？那种与不容批评联系<br />
<br />
在一起的赞誉，只是另一种“伟光正”，同样让人厌恶。对我们而言，更有意义的是对自身的反省，通过反省发现我们的错误，思<br />
<br />
考如何做得更好。这种反省即使不能改变历史，至少还可能改变未来，而且这可以由我们自己把握。<br />
<br />
&nbsp; &nbsp; 天安门运动有没有可以反省之处？从六四后的海外民运可以看得很清楚――当那场运动的各路领袖在自由世界云集，却把民主<br />
<br />
搞得一团糟，把民运从全球瞩目的巅峰搞到今日众人绕行的泥潭。二十年过去，当年用“年轻”为理由所原谅的，并未随年轻步入<br />
<br />
中年甚至老年而消失。领袖们如此，怎能让人相信运动本身无需反省？若是运动当年继续下去，后来民运发生的种种，可想也会在<br />
<br />
运动中显现（其实当时就已存在），并且放大多倍。<br />
<br />
&nbsp; &nbsp; 不容批评，不自我反省，正是民运的主要特征。柴玲状告长弓只是一例。对天安门运动的神化，让当事人不容他人批评，反过<br />
<br />
来也让他人可以抹黑当事人（只要发现其不符合神化）。说《天安门》影片破坏了柴玲形象，其实只是破坏了一个被神化的形象，<br />
<br />
并未破坏她作为普通人的形象。她在影片中有不少光彩照人的画面，称得上“硝烟中的甜美”。虽然她对记者的哭诉有些做作，但<br />
<br />
是那么年轻的女孩突然变成全球焦点，不做作才不正常。谁在年轻时会有宠辱不惊的定力？至于她说了不想死，那又算什么，哪个<br />
<br />
20多岁的孩子想死？众多对柴玲的批评，其实也是出自神化角度――她自己不想死，却“期待的就是流血”，让“广场血流成河”<br />
<br />
去擦亮中国人眼睛，柴玲的这种说法固然不够高大全，但是若能深入多数政治人物的内心，他们在同样时刻很可能也和柴玲想得一<br />
<br />
样。只不过那些人老练圆滑，不往外说，而 1989年的柴玲比较朴实，不但说了，还对着摄像机。<br />
<br />
&nbsp; &nbsp; 但是今日有了钱的柴玲，开始着手打击破坏了她神化形象的长弓，这才显示出真正问题。她控告长弓损害名誉未被法院受理，<br />
<br />
说明损害名誉不成立，只是损害了她想维持的神化――完美、不容置疑、却不真实。不过仅限如此，也算一种可以理解的个人意愿<br />
<br />
，无可厚非。但是当损害名誉告不成，还要用商标侵权的诉讼耗尽长弓的有限资财使之垮掉，就透露出整人和报复的用意。这不得<br />
<br />
不让人怀疑，能用金钱扼杀言论自由的人，一旦掌握了权力，会不会用权力去做同样的事？<br />
<br />
&nbsp; &nbsp; 这的确是一个重大的思考，倘若民主运动的代表人物有可能变得和他们反对的共产党一样，意味着什么？这其中暴露的危险，<br />
<br />
需要我们更深地追究，民主到底是什么？我们的民主代表者为何会这样？以民主为旗的天安门运动，自身是否找到了民主？而中国<br />
<br />
到底应该实现什么样的民主，以及怎样获得真正的民主保证？这些问题，都不是简单地用民主两个字就可以回答的。我们必须正视<br />
<br />
和发现民主光环背后任何可能的不良，潜在的专制，领袖的不端，以及人性的危险。正因为我们对民主寄予厚望，把自己与后代的<br />
<br />
未来托付给它，也就应该更加尖锐无情、不留情面地对其审视和追究。<br />
<br />
&nbsp; &nbsp; 但是我们没有做，或是做得很少。我们的感情陷得太深，或者我们有太多东方式的纠葛。长弓的外国人做了，虽然做得不一定<br />
<br />
足够，也不一定都合适，可以有不同意，却应该感谢、至少也要尊重他们。他们使我们思考，是在帮助我们。而那些指责影片不该<br />
<br />
针对柴玲的各种理由，说到底是一种对反省的回避，因为任何人都可以找到不被针对的理由，而没有针对的反省又如何进行？尽管<br />
<br />
柴玲那时年轻，却是天安门运动的代表形象，被反省涉及乃是其应得其所。享受桂冠的同时也得承受鞭笞，这就是公众人物的命运<br />
<br />
。而从另一角度，假若柴玲没有目前行为，说影片不该针对她倒也可以同情，然而正是她想搞垮长弓的诉讼，反倒显出了《天安门<br />
<br />
》以柴玲为剖析对象的预见性和必要性。<br />
<br />
&nbsp; &nbsp; 把柴玲说成弱者而反对《天安门》的，同时也是不容反省天安门运动的理由。因为天安门运动是弱势方，是无权者的反抗，是<br />
<br />
被镇压和流血的。但如果我们不是永远期待失败，不妨想想相反的可能，即假若有一天能成功呢？那么当初回避反省会带来什么后<br />
<br />
果？也许所有缺点在弱者身上都不构成威胁，但是弱者有一天变成了强者，缺点的存在就会立刻成为现实的威胁。在我们以往的历<br />
<br />
史中，小人物摇身而成暴君的故事难道还少吗？<br />
<br />
&nbsp; &nbsp; 至于具体的反省内容，当然可以讨论。譬如我就不觉得领袖品德是主要问题，也不认为可以用非理性来责怪群众，更不同意所<br />
<br />
谓民智未开是失败根源。在我看，真正的根源正是让领袖品德败坏、让群众趋于极端、让民智呈现未开一面的民主模式。责任不能<br />
<br />
归咎于人民，因为不能要求人民适应民主模式，而只能是民主模式去适应人民。当民主模式不适应人民而失败时，却得出必须等待<br />
<br />
人民适应了模式再行民主的结论，完全是本末倒置。那些停驻于只能启蒙的感叹，也是这样一种思路。<br />
<br />
&nbsp; &nbsp; 我所说的反省天安门运动，不是对人对事的责怪和抱怨，重要的是找到能够制约领袖行为、提炼群众理性，又可以让人民智慧<br />
<br />
生发而非局限放大的民主模式。民主价值是普世的，民主模式却不可能普世，因为不同的社会有不同的历史与文化，只能用不同模<br />
<br />
式去适应。若是从这方面反省，主要的责任者便是中国的知识精英。当年那些两手空空搬弄教条，现在仍未找到自己的精英们，无<br />
<br />
论是鼓吹实行民主的，还是断言没有条件的，二十年来仍在咀嚼他人的冷饭。正是因为没有对天安门运动深入反省，二十年后的我<br />
<br />
们仍然在原地打转。即使今天再出现一次那样的运动，谁敢保证我们有能力跳出二十年前的覆辙？放眼环顾，当年绝食的孩子们今<br />
<br />
何在？办了六四绿卡的十数万俊杰为谁忙？知识分子蝇营狗苟于精英联盟分羹，中国向何处去的百年提问却依然迷茫，而各种危机<br />
<br />
一天天加深。这令人唏嘘的二十年轨迹，那么多沉沦，那么多背弃，那么多迷惘与无奈，而除了赞美与谴责，又能不能让我们深入<br />
<br />
自身，面向自己，多一点反省呢？<br />
<br />
&nbsp; &nbsp; 走出过去，走向未来――我在长弓呼吁书上签名，也是为了表达这样一种希望。<br />
<br />
&nbsp; &nbsp; 2009-5-29<br />
<br />
&nbsp; &nbsp; （《民主中国》首发）<br />
<br />
&nbsp; &nbsp;<br />
<br />
&nbsp; &nbsp;附:呼吁书 纪念六四 兼论言论自由及其倡导者<br />
<br />
&nbsp; &nbsp; （长弓纪录片制作组，2009年4月15日）<br />
<br />
&nbsp; &nbsp; 值此1989天安门抗议运动20周年之际，我们怀着沉痛的心情纪念这一惨遭残酷镇压的民众运动。在这场运动中，数以百万计的<br />
<br />
中国民众曾走上街头，要求新闻与言论自由。<br />
<br />
&nbsp; &nbsp; 20年前，长弓制作组着手编导纪录片《天安门》并建立相关的历史资料网站（<a href="http://www.tsquare.tv" target="_blank">www.tsquare.tv</a>），旨在以独立的视角和深入浅<br />
<br />
出的方式表现这一历史事件的复杂动因及过程，并为学者和公众持续提供有关的研究资料。<br />
<br />
&nbsp; &nbsp; 1995年10月，《天安门》入选纽约电影节。影片尚未公映，就遭到来自中国政府和某些89学运参与者两方面的声讨。随后中国<br />
<br />
政府要求所有的国际电影节禁演此片，声称《天安门》“歌颂暴徒，误导观众，伤害了12亿中国人民的感情”。反对此片的学运参<br />
<br />
与者则一口咬定我们是受中国政府指使，斥我们“是一群苍蝇，是我们这个时代真正的疾病。”<br />
<br />
&nbsp; &nbsp; 尽管如此，《天安门》仍在美国和世界各地获得多项电影与学术大奖。影片至今依然受到媒体、有关研究人员以及教育界人士<br />
<br />
的关注，与其网站一道，已成为国际上有关中国现代史研究探讨的组成部分。我们深信，此类独立研究与独立影片的制作只有在学<br />
<br />
术界同仁、公共基金会及私人捐助的鼎力支持下，在言论自由得到充分保障的情况下才能得以进行。<br />
<br />
&nbsp; &nbsp; 然而今天，我们作为一个独立制片和历史档案机构的基本生存正在受到严重的威胁。令人深感担忧的是，这种威胁竟来自一个<br />
<br />
曾在1989年学生运动中声称支持言论自由和民主的人士。柴玲，原保卫天安门广场指挥部总指挥、尖子班（注：美国一商业软件公<br />
<br />
司 Jenzabar）现任总裁，与她的丈夫 Robert Maginn（原 Bain Capital的合伙人及director，现任尖子班首席执行官）于2007年<br />
<br />
在美国麻州波士顿以损害名誉和商标侵权两项罪名对我们提出了控告。<br />
<br />
&nbsp; &nbsp; “损害名誉”的指控基于我们的网站链接了美国一些主流媒体发表的文章，其中涉及到有关尖子班、柴玲和Robert Maginn的一<br />
<br />
些负面消息。关于商标侵权的指控，则仅仅基于我们的网站中引述相关文章时出现了“尖子班”这一名称。对于“损害名誉”的起<br />
<br />
诉，法院决定不予受理。对“商标侵权”的指控，法院认为：虽然由于两家公司业务不同，尖子班胜诉的可能性极小，但仍决定给<br />
<br />
予尖子班一次为其指控提供证据的机会。<br />
<br />
&nbsp; &nbsp; 我们的长弓制作组是一个非盈利性的纪录片制作公司， 而尖子班是一家为高等教育机构提供管理系统软件的商业公司。我们与<br />
<br />
尖子班的业务毫不相干，亦不存在任何商业上的竞争。尖子班却指控我们的网站侵犯其商标，误导其潜在用户，从而造成其公司的<br />
<br />
客户流失。我们并非商业软件公司，也不销售尖子班的客户可能感到兴趣的一类软件，而且事实上也从未有人就尖子班或其软件与<br />
<br />
我们进行过联系。<br />
<br />
&nbsp; &nbsp; 近年来，美国的一些主流媒体，如《波士顿环球报》、《福布斯》、及《高等教育学报》，都曾报道过并在网上存档尖子班及<br />
<br />
其总裁柴玲的情况。长弓网站仅止于引用了这些报道。据我们所知，首发报道的这些主流媒体并没有受到尖子班的任何指控。因此<br />
<br />
我们有理由认为，这次法律诉讼显然是旨在以要挟为手段，迫使我们从网站上删除与柴玲和尖子班有关的信息。柴玲的律师就曾明<br />
<br />
确要求我们从网站上删除一切有关尖子班的材料。然而我们认为，这些历史资料不仅有关公众利益，而且早已通过其它途径进入公<br />
<br />
众视听领域。为尽早结束这场在我们看来是惩罚性的法律诉讼，我们与对方进行了多次谈判并作出某些和解性让步，然而这场官司<br />
<br />
目前依然在继续，已经给我们带来巨大的财务损耗，以至威胁到我们这个机构的生存。 <br />
<br />
<br />
&nbsp; &nbsp; 以下是2007年5月对方提出的控告的摘录。 这些严重的指控既是对长弓制作组的威胁，更是对我们一贯所珍视的政治与言论自<br />
<br />
由等基本原则的威胁：<br />
<br />
&nbsp; &nbsp; “出于恶意和对共产党政府官员的同情，并一心想诋毁中国天安门民主运动的一名学生领袖柴玲，长弓制作组在其网站传播有<br />
<br />
关原告的虚假信息，从一些过时的报道中收集误导性言论，散播半真半假的消息，以期造成对尖子班、柴玲和Maginn的不良印象。<br />
<br />
为确保这些内容尽可能广泛地传播，以造成最大损害，长弓在未经授权的情况下使用受法律保护的尖子班商标，将读者引到长弓网<br />
<br />
站。后果之一是，尖子班的客户及潜在客户被误导到该网站和该网站上的诋毁性内容，由此造成对尖子班名誉的伤害和商业机遇的<br />
<br />
损失。”<br />
<br />
&nbsp; &nbsp; “我们获知并认为长弓的诋毁性言论出自对柴玲本人的恶意，图谋损害柴玲的声誉，以达到他们别有用心的政治目的。”<br />
<br />
&nbsp; &nbsp; 起诉书同时还要求：<br />
<br />
&nbsp; &nbsp; “长弓必须申报由前述非法行为所带来的所有赢利，并用此非法获得的赢利对尖子班进行陪偿。”<br />
<br />
&nbsp; &nbsp; 我们认为，尖子班这一指控，旨在伤害我们这个非盈利性的独立制片和研究团体，这个团体在过去三十年间为世界各地的广大<br />
<br />
观众与教育界提供了有关中国历史、人文风貌的各种影片。柴玲和尖子班似乎已下定决心，除非我们顺其要求从网站上删除历史资<br />
<br />
料并回避尖子班的名称，他们就要通过旷日持久的诉讼过程耗尽我们这个非盈利机构的有限财力。我们认为，他们的目的和手段不<br />
<br />
仅对我们具有极其严重的后果，同时在更大的范围内，对言论自由与独立学术探讨也具有深远的负面影响。<br />
<br />
&nbsp; &nbsp; 我们认为，在纪念六四20周年的同时，有必要反思与重申如下一些原则在我们自己所处的环境中的重要性。这些原则包括：独<br />
<br />
立思想、独立于任何政治与商业利益集团的历史研究、收集和保护历史文献、以及言论自由。<br />
<br />
&nbsp; &nbsp; 为此，我们向各位专家学者、各媒体及教育界的同仁们、以及关注此案的公众提出呼吁，请到我们的网站（<a href="http://www.tsquare.tv" target="_blank">www.tsquare.tv</a>）<br />
<br />
阅读有关这场法律诉讼的详细资料，以期就言论自由等有关问题作出你们自己的判断。<br />
<br />
&nbsp; &nbsp; 此呼吁书绝非针对尖子班、其产品或其所提供的用户服务。我们无意引起或参与任何抵制尖子班，或有损于尖子班 的营业与雇<br />
<br />
员的任何活动。我们只要求维护我们的权利，捍卫言论自由，保护我们自己和我们所从事的工作不受这一无理指控的伤害。<br />
<br />
