我不反对人们的正常自利行为，但我也不会鼓励它。我想像不到鼓吹自利和宣扬无私可以共存，如果鼓励的是自利，把宣扬无私看做唱高调，那我们就只能嘲笑刘晓波是个大傻逼了，这样的社会岂不是太犬儒、太冷漠、太无情了。而对于信奉（不是嘴上说说，而是落实到行动中的）人不为己天诛地灭的人，我对他们没太多话可说。<br />
