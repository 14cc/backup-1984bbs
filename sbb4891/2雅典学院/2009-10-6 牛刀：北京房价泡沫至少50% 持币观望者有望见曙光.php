																											<div id="postmessage_271865" class="t_msgfont">牛刀：北京房价最少50%泡沫 低价购房者曙光来临<br />
<br />
2009年10月06日14:23<br />
<br />
<img src="http://i2.sinaimg.cn/hs/news/2008-09-18/U2939P643T3D73292F72DT20080918182538.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　房地产独立评论人牛刀近日表示,北京房价至少有50%的泡沫,房价会出现一个明显的拐点,购房者应该继续等下去。<br />
<br />
　　牛刀认为,现在楼市进入了一个空转的时期,房价虽然还稳得住,但是它的成交量上不去,因为老百姓的购买力很低、很弱,购买力跟不上。<br />
<br />
　　牛刀表示,土地泡沫是中国泡沫最严重的泡沫。他表示,开放商自由资金很低,通过金融杠杆,用很少的钱显现拿地,影响了一个更大的市场。中国的房地产商没有固定资产的,本身不持有资产,龙头老大万科总市值中固定资产只占5%,其他的都是开发的流转的土地。<br />
<br />
　　以下为凤凰卫视10月3日《一虎一席谈》节目“牛刀:北京房价至少有50%的泡沫”文字实录:<br />
<br />
　　胡一虎：一开始就要问牛刀,牛刀你最近也是一个新闻人物,你提出了很多就讲说中国楼市在目前来讲,进入新的拐点。但是我们讲新的拐点,总要有一个证据,我们首先请全球华人,来看我旁边的这个题板。这是北京,我们先从北京来看,北京的商品房、住宅、办公,商业成交均价最近的走势的比较。<br />
<br />
　　我们来看一下从今年2009年的1月,到我们所掌握的资料,到8月份为止,它的整个走势,我们看蓝色的部分是商品的住宅,蓝色的部分你可以看到,它的走势是属于这种局面来讲,其实是相当之平稳,也并没有说特别得走高,我们可以发现是蓝色的部分,这个部分是整个成交的这个均价的部分。<br />
<br />
　　在价格方面来讲,您可以看出来,并没有很明显的上涨,尤其在最近这一块当中。我们再来看一看下面的,是整个,是成交量在最近这几个月的走势来比较,如果你从成交量一看,蓝色走势图这边好像往上爬,到这边稍微盘整一下,稍微高走一点,现在还低下来了。那么到这个部分,是一百五十九万点三四的这个平米,我们看一下,从这两个图当中,当然图本身它意义有说话。牛刀从这个部分来讲,说明了什么？<br />
<br />
　　牛刀:这个说明市场的正常反应。<br />
<br />
　　胡一虎:市场的正常反应,为什么会出现我们发现,量好像是缩了,但是价格还是有点高的？<br />
<br />
　　牛刀:它现在楼市进入了一个空转的时期。<br />
<br />
　　胡一虎:空转的时期。<br />
<br />
　　牛刀:房价它还可以稳得住,稳得住,因为我们的货币流动性还在。但是它的成交量是上不去,因为我们老百姓的购买力很低、很弱,购买力跟不上。那么购买力跟不上,流动性又在,促使了它的房价还在空转,但是成交量大幅地萎缩。<br />
<br />
　　胡一虎:你刚才是用的是成交量,你就是讲整个房市在属于空转的这个情况来讲。但我们难免会做对比,比如说这个时刻,比如九月份跟十月份,大家觉得在房市上表现得,就是金跟银的代表,代表应该是非常旺季。但是从最近的一些相关的走势来讲,似乎好像没有反映出来这个部分？<br />
<br />
　　牛刀:没有,这个金九银十呢,这个概念提出很久,但是从今年以来,我们发现它的最王的季节是五六月份,四五六月份,但是进入七八月份,整个楼市的成交量就开始萎缩。<br />
<br />
　　胡一虎:等一下,牛刀,牛刀其实大家有这个愿望,其实一般老百姓在看的时候,就觉得说也有个想法,我就这样等好了。但是,是不是等的时机会出现,现在一个反转,你的理据是什么？<br />
<br />
　　牛刀:市场的问题要由市场来解决,那么市场它最后怎么来解决这个问题呢？就是央行回收货币流动性,央行回收货币流动性,是在通货膨胀发生的情况下,一发生了通货膨胀,央行就回收货币流动性。第一个降低存款准备金,提高存款准备金率;第二个加息。只要有这两点,房价也会出现一个明显的拐点。<br />
<br />
　　胡一虎:明显的拐点。<br />
<br />
　　牛刀:所以我觉得,现在等到房价下降的人,是有希望的。<br />
<br />
　　胡一虎:是有希望的。<br />
<br />
　　牛刀:已经看见了曙光。<br />
<br />
　　胡一虎:看见曙光,等一下,那对大姐来说,她现在还要继续等下去吗？是会出现暴跌的情况吗？<br />
<br />
　　牛刀:我认为还会继续等下去,像北京房价的泡沫至少有50%。<br />
