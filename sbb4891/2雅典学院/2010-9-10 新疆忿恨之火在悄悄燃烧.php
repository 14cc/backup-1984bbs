													<h2>新疆忿恨之火在悄悄燃烧</h2>
						
						
																											<div id="postmessage_823695" class="t_msgfont">纽约时报：中国新疆 忿恨之火在悄悄燃烧<br />
<br />
Posted by xiaomi2020 on 2010年09月7日<br />
<br />
核心提示：种族歧视、居民条例和护照限制了维族人出国和在国内的迁徙<br />
<br />
原文：Resentment Simmers in Western Chinese Region - NYTimes.com<br />
<br />
作者：ANDREW JACOBS<br />
<br />
发表时间：2010年9月4日<br />
<br />
译者：Ree<br />
<br />
校对：@xiaomi2020<br />
<br />
Shiho Fukada 为《纽约时报》拍摄 更多图片<br />
<br />
维族女人在中国西部新疆和田的一个集市上采购。2009年，维汉之间的种族冲突中有近200人死亡。<br />
<br />
中国、乌鲁木齐――五星级酒店爆满。大街上，推土机们迫不及待地要铲平沉默的平民窟，“法式别墅”的广告牌向暴发户们发出呼唤。那场有近200人死亡的维汉种族暴乱已过去一年，在这座中国西北城市，生活似乎重归太平。<br />
<br />
“别担心，现在平静的很。”在乌鲁木齐的汉人区，一位活泼的侍应生这么说。<br />
<br />
没等转身，小伙子又开口了：“但你最好别在晚上去维吾尔区。”他说。<br />
<br />
在乌鲁木齐平静、繁荣的外表下，这个新疆的首府有着一种明显的紧张，无论是成千上万的摄像头，还是穿黑衬衫的巡警们都无法完全缓解。<br />
<br />
2009 年7月，维吾尔族暴徒首先用铁条和板砖袭击汉人，接下来的几天是汉族治安队的反击和复仇。中国政府一方面逮捕了上百名疑犯，一方面抛出15亿元的财政补贴来舒缓紧绷的神经。自治区主席被更换，宣传和谐的标语贴满了公交车两侧和高速路桥。但是和谐的宣传和蓄势待发的经济修复不了民族之间的互不信任。专家们认为，中国政府开展“严打”运动，秘密关押“闹事者”，处决至少9名涉嫌参与血腥事件的“暴乱分子”，反而使紧张局势进一步恶化了。<br />
<br />
“我认为没有一个维族人会觉得政府代表的是他们的利益”，加州波蒙娜大学（ Pomona College）的研究新疆问题的亚洲专者 Dru C. Gladney总结道，“实际上，充满敌意的环境让人们如置身战场般紧张和不安”。<br />
<br />
在监控不断加强的情况下，无论是询问维族人，土耳其语系的穆斯林少数族裔，还是占中国人口96%的汉族人，你总是很难得知他们的真情实感。但只要你足够耐心，并且保证匿名，真实的怨恨最终会浮现。<br />
<br />
以一个汉族的小餐馆老板为例：采访开始，他还说着“维汉一家人”，但后来，大部分的维族人在他口中就成了可怕的，不值得信任的“野蛮人”了。<br />
<br />
这位只肯公开其姓的周店主说，自从去年秋天，“维族人用艾滋毒针扎公车上的汉人”的谣言闹得满城风雨以后，他就再也没敢再坐公交车。警方起初逮捕了十数个参与攻击的疑犯，稍后又否认针上携带病毒的说法。<br />
<br />
2004年离开四川的周先生说，就像每年成百上千移居新疆的汉人一样，他部分也是受到“西部大开发”的理念所鼓舞。他从小就被灌输说，维族人也是中华民族的一部分，是万花筒般的56个民族之一，但来到新疆以后，他发现根本不是这么回事。<br />
<br />
“我们真是没多少共同点”，他一边小心地瞄着周围一边说，“更糟糕的是，他们不感激我们为他们所做的”。<br />
<br />
就像对付另一个动荡的少数民族区――西藏一样，中国政府砸进大把大把的资金来帮助新疆的经济赶上东部――那里的人民收入与人均生产总值平均是新疆的两倍。今年5月，中共正式启动了“热爱伟大祖国，建设美好家园”行动，其中包括新建6个机场与5,000多英里的铁路，以连接一块阿拉斯加大小的荒漠山地中的边陲城市。<br />
<br />
鉴于普遍的不满，北京更换了自治区主席王乐泉。王15年的强硬执政最终疏远了维族人、以失败的暴乱预控与汉人的游行示威而画上不光彩的句号。<br />
<br />
维吾尔人在1949年曾占到新疆人口的90%，现在却不到2200万人口的一半，他们只能将仇恨深深埋在心里。他们认为，北京一边决心肢解维族文化，一边让汉族移民得到最好的工作，特别是在政府和生产建设兵团下属的欣欣向荣的工厂里，建设兵团是解放军在1950年待在沙漠中建成的相对隔绝的汉人军事前哨战。<br />
<br />
确有一些维族人因为不会说普通话而不能进入汉人控制的公司，但许多人认为，在经济上边缘化的外表下更重要的原因是赤裸裸的种族歧视。“以前国企有只收汉人政策，但是现在含蓄多了” ，Ilham Tohti，一位研究新疆就业市场的维族经济学家说，“他们只在你得到面试，看到你本人之后才拒绝你的申请”。<br />
<br />
尽管低学历和非技术人员是经受失业打击最大的一群，但即便是大学毕业，会说维汉双语的维族人，也表示他们很难找到好工作。<br />
<br />
一种“身陷重围”感更加重了维族人的失望情绪。他们囿于顽固而严格的户籍制度，无法移居富裕的沿海城市，严格的护照限制则阻止了他们离开中国。<br />
<br />
中国政府制定这样的政策，部分是为了防止维族人出国之后可能变得激进，回来后成了恐怖分子。但这种政策却让维族人失去到海外工作，学习的机会，让他们与家人难以相聚，也使想在中亚贸易分得更大一杯羹的维族商人感到失望。“要是我们连出国去见客户都不行，我们怎么和汉人竞争呢？”一位来自南部绿洲城市，卡什干，的纺织商人这样说到，“难道因为我们是穆斯林，就都喜欢当恐怖分子吗？”<br />
<br />
政府对伊斯兰宗教极端势力的担心化为了更严酷的宗教政策，包括前往沙特麦加朝圣的人数限制，与伊斯兰斋戒月时强制让学生和政府工作人员进食。<br />
<br />
恐惧也主宰了俗世生活。电话与邮件常被监控，对政府政策最小的牢骚可能要承担最严重的后果。7月，三名维族网络编辑和一位记者在闭门审判后以“危害国家安全罪”判处长期徒刑。<br />
<br />
