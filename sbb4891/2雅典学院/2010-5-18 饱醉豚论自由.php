<br />
1）中医有许多经验处方和疗法，但考虑到中医界迄今普遍顽固拒绝现代实验和统计方法，这些经验知识的可信度不高，大致上仍处于前科学水平；<br />
<br />
2）前科学的、未被组织到某个科学理论体系中的经验知识，未必“无用”，我认为在现代科学出现之前，有大量有用的经验知识；至于中医的经验知识有多少、在多大程度上有用，因为（1），无从作总体判断，只能依赖个人信息背景，各自个别判断――这也正是前科学时代的特点所在；<br />
<br />
3）中医的理论体系――比如阴阳五行学说、经络学说、寒热干湿甘苦之辩等等，未构成自洽而可检验的科学理论，其理论和处方/疗法之间，也缺乏最起码的稳定明确的逻辑关系；基于此，我认为该理论体系没有任何保留价值；<br />
<br />
4）对于“是否应该废除中医”，我的回答视“废除”一词的含义而定：<br />
<br />
5）如果废除是指废止中医医师的行医执照，我赞成，我主张废止所有医生的行医执照，我认为行医无需执照；<br />
<br />
6）如果废除是指禁止中医医师行医，我反对，我认为任何有完全民事行为能力的人都可以行医；<br />
<br />
7）如果废除是指禁止医师使用未经现代医学界认可的中医处方和疗法，我反对，我认为医生在审慎、充分告知和患者自愿原则下，可以使用任何他认为恰当的处方和疗法，上述三条原则是否得到遵守，必要时应在法庭上认定；<br />
<br />
8）如果废除是指撤销政府对中医事业（研究、教育、资源等）的资助，我赞成，我并且赞成政府撤销对一切医疗事业的赞助；<br />
<br />
9）如果废除是指禁止中医服务被纳入保险合约，我反对，首先，医疗保险合约不应由政府提供，其次，合约覆盖内容应由保险公司自行决定；<br />
