																											<div id="postmessage_622578" class="t_msgfont">饱醉豚论自由（最新版）<br />
<br />
发信站：格道网 <a href="http://www.gettao.com/" target="_blank">http://www.gettao.com/</a><br />
谁是自由的敌人？不存在一个纯粹的自由主义国家，但是自由主义者要争取足够的自治权：不是按照地区或宗教或种族划分的自治权，而是纯粹基于个人价值信仰的自治权。在个人和基于共同价值的小团体和国家签订权利义务契约成为现实的情况下，自由主义社区可以得以实现。<br />
<br />
<br />
谁是自由的敌人？自由主义反对大一统的价值观，接受并容纳任何价值观。科学主义或纳粹主义或共产主义都应该有自己的一席之地。多数情况下，不是自由主义不能容忍别的主义，而是别的主义不能容忍自由主义。<br />
<br />
<br />
谁是自由的敌人？用左右来分立场，是一种非常愚蠢的制造冲突的办法。但是大多数人都逃离不了这种白痴思路，制造二元判断，便于可以区分立场。人的价值观是多维的，可称之为价值矩阵。自由主义习惯于各种价值共存，而不赞成左右对阵。 <br />
<br />
<br />
谁是自由的敌人？那些抱怨自由主义者一会儿左、一会儿右的人，完全不懂自由主义。自由主义者只有一个立场：那就是把自由作为非常高的价值，远远高于民主、福利、社会安定、健康长寿、人民满意、生活富足之类的价值。衡量自由主义者的立场，是看他把自由价值摆在多高的位置。 <br />
<br />
<br />
谁是自由的敌人？自由主义是一种信仰。正如其他的信仰一样，它本身不是一种可以直接拿来建立国家体制的东西（实际上也没有任何一种主义本身可以建立一个国家，一个具体的制度永远是动态博弈的结果）。但是在政治博弈力量中，自由主义的力量并不大。 <br />
<br />
<br />
谁是自由的敌人？自由主义只适合极少数人，绝大多数人不会把自由摆在一个很高的位置。而民主社会永远是大多数利益吞掉少数人自由的社会。在一个大一统的民主制度下，指望拥有足够的消极自由是不可能的。他们唯一能给你的自由，是那些大众认为自己也可能需要的权利。 <br />
<br />
<br />
谁是自由的敌人？绝大多数神化民主的人都是自由的敌人。跟独裁专制斗争的时候，提倡民主是有斗争价值的，但是一旦涉及民主和自由的关系，则民主就是自由的敌人。现存所有的民主政体，都和独裁专制一样，都是强迫个人接受大众民主的结果，不承认个人的自治自由的。 <br />
<br />
<br />
谁是自由的敌人？美国总统罗斯福说：人有免于匮乏的自由。罗斯福这种人是自由最可怕的敌人之一。这种论调隐蔽，有着正义的形象，容易被最大人民接受，并能煽动大众为了自身的不匮乏去剥夺别人的自由。积极自由不是自由，这是基本原则。 <br />
<br />
<br />
<br />
谁是自由的敌人？如果为了绝大多数人免于恐惧，而禁止器官买卖，这是典型的大众利益侵犯个人消极自由的行为。在独裁国家，可以任意以大众利益的借口剥夺自由，在民主国家，就是典型的民主暴力剥夺个人自由。 <br />
<br />
<br />
<br />
谁是自由的敌人？某人说如果人体器官允许在市场上自由买卖，大众的生命权就保不住，就有人会买卖他人的器官。这种人是自由的敌人。以大众利益剥夺个人自由，是最好的扼杀自由的借口。 <br />
<br />
<br />
谁是自由的敌人？民主是自由的敌人。民主从来不因为你不愿意参与他们的民主游戏而给你自由，一旦他们的民主游戏有了结果，必然会强加到你的身上。甚至某些民主国家会立法规定：你不参与他们的民主游戏，比如不参加投票，本身就会被判有罪。 <br />
<br />
<br />
谁是自由的敌人？有人说如果正义凛然的人不自以为是，保持清醒的头脑。坚持集体领导原则再加上一些民主元素， 就不会成为自由的敌人，有这种观点的人是自由的敌人。民主权力或独裁权力，都可能是自由的敌人。集体领导更是自由的敌人，因为侵犯人的自由，却让人连复仇的对象都找不到。 <br />
<br />
<br />
谁是自由的敌人？绝大多数人民都是自由的敌人。他们喜欢高福利带来的种种安全感，喜欢免费医疗、免费教育、免费住房，失业了要求政府解决他们的工作问题，他们讨厌游手好闲的流浪者，讨厌邪教，讨厌聚众淫乱者，仇视那些极端蔑视社会伦理道德的人。 <br />
<br />
<br />
谁是自由的敌人？如果有人跟你说：世界上没有绝对的自由。这话最值得警惕，因为这话实际上就是说：任何自由都是随时可以用各种借口剥夺的。对自由主义者来说，能接受的底线是：只有导致直接危害他人的情况下，群己权界可以商量妥协，但是界限里面的全是绝对自由。 <br />
<br />
<br />
谁是自由的敌人？长江高价捞尸案中，把索要高价的当事人治安拘留15天的公安部门，是自由的敌人。 <br />
<br />
<br />
谁是自由的敌人？一般来说，正义凛然的人比常人更容易沦为自由的敌人。因为他们非常强烈的正义感，使他们比常人更难以忍受与主流正义价值不符合的行为和观念。比如包公和海瑞，一个杀掉了陈世美，一个饿死了自己的女儿，都是典型的反自由人士。 <br />
<br />
<br />
谁是自由的敌人？有人认为：为了大多数人而选择自我牺牲的英雄是伟大的，他们的英雄事迹不容亵渎，这些人是自由的敌人。自由主义者应该认为这是他们选择了自己的审美利益最大化，跟别人选择苟且偷生一样的自愿选择，我们并没欠他们什么。 <br />
<br />
<br />
谁是自由的敌人？那些享有充分的自由来指责你的自由的人，他们享受自由的同时反对自由。 <br />
<br />
<br />
谁是自由的敌人？每个人都可能是自由的敌人，人是审美动物，而不是自由动物。自由只是价值审美的一维，自由主义者这方面的权重比较大。自由本身也是多维的，各维也会冲突。消极自由的限定是设法消除人和人之间的自由的冲突，不能消除自由自身的各维的冲突。 <br />
<br />
<br />
谁是自由的敌人？那些规定了很多复杂礼仪，制定了很多节日的人。他们指责你情人节不送玫瑰，指责你清明节不上坟却喝花酒，指责你忘记了结婚纪念日，指责你中秋节不跟家人团聚，指责你居然不知道父亲节母亲节。在他们看来，每一天应该干什么，别人得跟他们一样。 <br />
<br />
<br />
谁是自由的敌人？那些反对自由的人和歌颂自由的人，都可能是自由的敌人。自由最大的敌人，不是那些说自由不好的人，而是那些认为自由神圣完美的人。一旦他们发现某个自由不好，就会说那不是自由。 <br />
<br />
<br />
谁是自由的敌人？立法规定最低工资的，强迫人人买医疗保险的，实行强制性的义务教育的，实施强迫性的计划生育的，禁止出口粮食给流氓国家的，是自由的敌人。 <br />
<br />
<br />
谁是自由的敌人？认为Ｆ－Ｌ－Ｇ让人生病不吃药危害他人身体健康，所以要禁止这类有害观点传播的人，是自由的敌人。认为星相风水算命看相测字是骗人勾当，应该禁止的人，是自由的敌人。 <br />
