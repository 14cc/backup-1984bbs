													<h2>杨宪益：反诗与愤世的无奈</h2>
						
						
																											<div id="postmessage_356939" class="t_msgfont">（东方早报又一无间力作！）<br />
<br />
<a href="http://epaper.dfdaily.com/dfzb/html/2009-12/04/content_175323.htm" target="_blank">http://epaper.dfdaily.com/dfzb/html/2009-12/04/content_175323.htm</a><br />
<br />
<br />
在被病痛折磨了几年后,当代翻译泰斗杨宪益先生以94岁的高龄于日前驾鹤仙去，结束了他“去日苦多”的人生，这对他来说未尝不是一种解脱。杨先生一生的名山事业，主要是把中国典籍翻译成外文。他和他的英籍夫人戴乃迭女士穷半个世纪的精力，翻译了包括《离骚》《红楼梦》和《鲁迅选集》在内的一大批古今中国文学名著。这些翻译作品，成为欧美世界了解中国文学的重要渠道。<br />
<br />
　　也许是无心插柳吧，杨六郎（杨诗里自称）暮年竟然以打油诗享誉京华，实在是件有趣的事情。这和聂绀弩晚年也以“聂体”怪诗为世所重，有异曲同工之妙。杨诗尤愤世嫉俗，针刺时弊，直指丑恶，往往能一针见血。<br />
<br />
　　何家干 <br />
<br />
　　<br />
<br />
　　杨宪益辞世后，检点藏书，杨宪益先生的书有好多种。有早期关于文史的《零墨新笺》，有谈翻译经验的《译余偶拾》，还有翻译过来的自传《漏船载酒忆当年》。这其中，最让我印象深刻的是杨先生的旧体诗作。在国内，杨的旧体诗作前后出过两次：一是1996年广东教育出版社出版的《三家诗》，除杨先生外，还收了黄苗子和邵燕祥两家，这本书现在已颇不好找；二是2007年福建教育出版社出版的《银翘集》，这大概是1995年香港天地图书出版的《银翘集》的内地删节版。<br />
<br />
　　也许是无心插柳吧，杨六郎（杨诗里自称）暮年竟然以打油诗享誉京华，实在是件有趣的事情。这和聂绀弩晚年也以“聂体”怪诗为世所重，有异曲同工之妙。当然，杨是打油，聂则格律谨严，体例不同，但在内容和遣词上却都能跳出旧诗藩篱，新潮得烫手。杨诗尤愤世嫉俗，针刺时弊，直指丑恶，往往能一针见血，若是放在那个非常年代都是可以断送老头皮的。到底赶上了政治清明的时代，竟然以删节本能公开印行出来，这不能不说社会有了长足的进步，是值得高兴还是应该悲哀？<br />
<br />
　　手头这本《银翘集》1996年购于香港，现在已经绝版，当是全本。现在信手抄录几首，略加评说。<br />
<br />
　　<font color="red">1990年中国第一次承办亚运会，而之前云波诡谲的政治运动是很难不让天真的知识分子目晕神眩的，要看清真相，其实很难。现在看来，书生干政，大多会被人当枪使，杨也许亦是此类。诗云：</font><br />
　　<font color="red">一　青海归来<br />
<br />
　　悬旗结彩迎佳宾，青海归来气象新。<br />
<br />
　　莫怪人人迎亚运，西游演罢是封神。<br />
<br />
　　二　怀苗子郁风<br />
<br />
　　世事如今尽倒颠，羡君海外获桃源。<br />
<br />
　　迎来亚运强充胖，一见华侨便要钱。<br />
<br />
　　人血馒头难续命，狗皮膏药岂延年。<br />
<br />
　　会看三峡功成日，一片汪洋浪接天。</font><br />
　　前诗最后一句乃鲁迅诗句，当时的热闹场面，也真如此般。一年前的奥运，又是以新的面貌如火如荼，而国人的激情却日渐消磨，如今承办亚运会不过寻常事耳。至于三峡工程，屡议屡寝，最终上马。在知识分子群中，<font color="red"><font size="4">很多人对三峡工程颇有微词。“高峡出斜湖，汪洋浪接天”已是事实。<br />
<br />
　　《全国第五次文代会》――1988年作<br />
<br />
　　周郎霸业已成灰，沈老萧翁去不回。<br />
<br />
　　好汉最长窝里斗，老夫不吃眼前亏。<br />
<br />
　　十年风雨摧乔木，一统江山剩党魁。<br />
<br />
　　告别文坛少开会，闲来无事且干杯。</font></font><br />
　　这里面的今典但凡稍熟悉中国文坛的都会知道。从1955年反胡风运动开始，文化界运动此起彼伏，猛烈程度较任何领域都烈。周扬在其中的推波助澜，“功”莫大焉。“党魁”名之，比祝贺谌容的皮里阳秋句子“从此夫荣妻又贵，将来一定做高官”更加直露。类似的句子还有不少，如盛传钱钟书提议修改的句子：“有酒有烟吾愿足，无官无党一身轻”等抨击当时的文化现象和社会风气的也有不少。1993年，《废都》热销，有诗云：<br />
<br />
　　忽见书摊炒废都，贾生才调古今无。<br />
<br />
　　人心不足蛇吞象，财欲难填鬼画符。<br />
<br />
　　猛发新闻壮声势，自删辞句弄玄虚。<br />
<br />
　　如何文字全除净，改绘春宫秘戏图。<br />
<br />
　　《废都》初读之下，是有故弄玄虚之感。让人尤不能满意的是里面那么多让人气闷的空格。“贾生”一是玩噱头，同时也是免得填空而露怯。《废都》还是自有其价值，虽然作者的妇女观非常陈腐。那时的“猛发新闻”和现今的文人不择手段炒作相比，简直连小巫也算不上。此老要是看到目下用身体写作，靠艳情开路，拿绯闻炒作，甚至造谣撞骗的文化名流的所为，不觉得对“贾生”太苛乎？<br />
<br />
　　有首《无题》大概是讥刺某位以写意手法来画古典人物的名画家的：<br />
<br />
　　不爱江山爱美人，范卿此去倍伤神。<br />
<br />
　　谁知西海瑶池宴，未及东瀛番主恩。<br />
<br />
　　从逆臣非金圣叹，陈情妾比玉堂春。<br />
<br />
　　一番会审官司了，幸喜当朝有后门。<br />
<br />
　　其人在那场政治风波前后的反复和“文革”期间背恩反噬师长的行径，向来为人所诟病。现在偶于电视中，见到此君手捧大烟斗，纵论中国艺术和儒家文化，颇能发人一嘘。对文化现象心怀不满的句子还有<font color="red"><font size="3">，“而今书肆多奸盗，高论原来不值钱”“干部无聊卖书号，官僚只管盖图章”“苍天已死黄天立，何必多余说扫黄”等。这些句子，已经远远追不上现今日益沦落的恶俗文化和社会现象了，只是我们再也没有诗人写出新的清平调</font></font>。<br />
<br />
　　直指社会的丑恶，与主旋律或主流媒体唱反调的诗，集子里多在。有的诗句竟到了直指鼻梁地步，看来确实淋漓痛快。<br />
<br />
<font color="red"><font size="3">　　《读报》――1995年作<br />
<br />
　　洒洒洋洋几万言，猪蹄猪爪一锅端。<br />
<br />
　　可怜治国安邦策，不及还阳补气丸。<br />
<br />
　　幕后正忙办丧事，台前依旧跳加官。<br />
<br />
　　退休干部闲无事，却道葡萄那个酸。<br />
<br />
　　大凡看惯报纸和官方新闻的人，读了这诗当会会心一笑。<br />
<br />
　　讽刺贪污腐败的《有感》――1992年作<br />
<br />
　　居然死水起波澜，赔尽长安体未安。<br />
<br />
　　自古贪污皆大款，而今调控靠宏观。<br />
<br />
　　早知肉腐虫先在，谁道唇亡齿便寒。<br />
<br />
　　总是自家妻女事，雷声虽猛早收关。</font></font><br />
<br />
　　贪污腐败，早成痼疾。近年来风气越演越烈，新蛀虫刚入罗网，大硕鼠又粉墨登场，大有杀身不足惜有钱就敢捞的英勇气概。和十多年前的贪污相比，现在让人拍案惊奇的事情更多了，花样更加翻新，情形越发严重。道德沦丧，吏治的腐败，遍及华夏，已经到了触目惊心的地步，而相关部门的公信力每况愈下，更是让人心寒。“早收关”的事情层出不穷，这样的事情看多了，只好是“是非论定他年事，臣脑如何早似冰”。<br />
<br />
　　有的句子，简直让人无法深入分析，如<font size="3"><font color="red">“江山今日归屠狗，冠带当朝笑沐猴”“千年古国贫愚弱，一代新邦假大空”，充满不平和怨愤，类似的诗句很多。何须深文周纳，退回三十年，不用打板子，就可直接打成反革命了。</font></font><br />
　　愤世有什么用呢？不过是文人觉得不平而鸣罢了。书生意气常被人看成是迂腐。但有些异样的声音毕竟是好的，至少能让后人知道，这个时代并不总是风花雪月，铁板一块。这样的诗，是不该从艺术的角度来看的，它毕竟不是无病呻吟的吟风弄月之作。<br />
<br />
　　到底是打油诗，难脱油滑，其中也有些诗实在不够温藉，<font color="red"><font size="3">如写某次导弹发射失败的一首：“新春烟火又登场，欲显神威震远方。导弹升天成烈炬，这般爆竹世无双。”</font></font>作这样的诗歌，的确是有失厚道的，这也算杨诗中的瑕疵之作。这样的诗在聂绀弩的集子里是绝对找不到的。也许是出身和遭际的不同，杨的骨子里还是个名士，自由知识分子。和聂有过红色炼狱的经历是完全不同的。聂诗更多的是对人性的洞察和针砭，而杨诗抨击的多是恶浊的社会现象。<br />
<br />
　　“天运苟如此，且进杯中物”，杨先生的一生是好酒的，能在微醺中面对这个世界与其说是刻意的选择也是一种无奈，他的晚年大约是厌倦了世情，在生命的最后几年里，基本上没见到他的任何文字。历经磨难，连独子也横遭迫害而自焚，这样的人生是常人不能承受的。1999年夫人戴乃迭故世以后，杨先生也停下了翻译的笔。他担任了二十年主编之久的英、法文版《中国文学》也于2001年停刊。这本于1951年创刊，出版了590期，绵延了半个世纪的刊物的谢幕，关上了中国文学面向西方世界的一个重要窗口。在让人扼腕的同时，不能不说在当下中国，这个商业大潮澎湃的时代，文学的衰落和精神的贫乏已经成为不争的事实。<br />
