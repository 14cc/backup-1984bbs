先回忆一下，我们在学校学过什么？ <br />
&nbsp; &nbsp;&nbsp;&nbsp;上课先举手， <br />
&nbsp; &nbsp;&nbsp;&nbsp;起立回答问题， <br />
&nbsp; &nbsp;&nbsp;&nbsp;上队课要敬少先队队礼， <br />
&nbsp; &nbsp;&nbsp;&nbsp;穿校服， <br />
&nbsp; &nbsp;&nbsp;&nbsp;这是我们学过的最基本的行为准则。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;而这一切，都是因为来自于纪律。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;纪律来自于专制，而专制强调的是集体主义。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;很多人以为的独裁，是个人的独裁，是偶像的崇拜，其实独裁是集体的独裁，是每一个人的参与，集体的独裁排除异己，想想，我们都曾经做过什么？ <br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;德国影片《浪潮》中的高中学生们对于社科老师的关于第三帝国和纳粹主义的讲课感到很没意思，认为现在的人已经不会再重蹈那样荒唐的覆辙了。老师为了给他们留下深刻印象而开始一项实验，让同学们体会到生活在极权主义当中究竟会出现何种情形。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;这是一部根据真实的事件改编的电影。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;一切来自于1967年美国加州Palo Alto Cubberley高中发生的真实历史事件。 <br />
<br />
赖纳是一位倡导自由民主的高中教师，本打算在以“国家体制”为主题的活动周上为学生们讲述无政府主义，但因为课题被同事抢先只能事与愿违的讲解独裁专制，为了让习惯了享受自由民主的同学们对这个课题有个感性的认识，赖纳开始了一次体验专制独裁的试验，随着一天天试验的进展，一个名为“浪潮”的集体也自然产生了。软弱、木讷、缺少家庭温情的蒂姆，生活在单亲家庭、渴望关注的马尔科，自卑而渴望平等的丽萨，来自移民家庭、被社会歧视的锡南，物质生活富裕但精神空虚的彦斯......一个个在现实生活中有着各种各样问题的个体因为“浪潮”的出现有了一个可以奋斗的理想和目标，在“浪潮”中大家互相信任、互相帮助，每个人都找到了对集体的归属感，然而当开始的热情逐渐升温，在不知不觉中转化为对浪潮的狂热时，由此带来的恶果也接连出现，赖纳与妻子感情日趋冷淡、马尔科与卡罗尔渐生隔阂，当赖纳最终醒悟并决定在活动周最后一天结束“浪潮”时，平时软弱、木讷的蒂姆，无法接受视为生命的浪潮将被结束的现实，在射伤同学之后开枪自杀。 <br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;专制让所有的人都卑微，让所有的人都平等，这都让我们想起没有过去多远的年代，为什么专制会让人狂热，当我们穿着整齐划一的服装、有着整齐划一的手势、整齐划一的口号、整齐划一的目标、做着整齐划一的人。我们为什么会热血沸腾？ <br />
&nbsp; &nbsp;&nbsp;&nbsp;我们不要给自己找借口，说自己是受到了蒙蔽。德国人正视了这一问题，正视了自己的责任。《浪潮》中的独裁者只不过是集体中的每一个个体寻找自我价值的梦想汇合在一起形成的偶像，当他与每个个体的梦想不再一致时，也是无能为力的，最终行使独裁专制权利的是整个集体，当每一个集团分子都陷入狂热之后，个人又能改变什么呢，这也就是为什么到了最后，赖纳――“浪潮”的创建者、独裁者和精神领袖，也无法控制局面的原因，最后已然不是独裁者控制个体，而是个体开始推动独裁。 <br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;人类是一种需要认同的群居生物。 <br />
&nbsp; &nbsp;&nbsp;&nbsp;当卑微的人没有能力去发掘出自我价值的时候，他就开始趋同于其他主流人群或者非主流人群。他需要一种归依感，尽管这种归依感是虚无的，但只要他觉得自己是混在人群中的人，他并没有被人群抛弃，那么他就得到了满足。越是那些怪异行为的狂热者，尽管看来和主流人群格格不入，却相反的，更是证明了他们是一群被“同质化”的人。这让我想起另一部美国电影《搏击俱乐部》。 <br />
