纳粹速成，只需五日。<br />
<br />
德国一中学的活动周上,教师Ron Jones开了一门讲述独裁政治的课。多数学生认为在今时今日,第三帝国式的统治和独裁者已经过于遥远,独裁主义缺乏民众基础，不可能在德国重演。赖纳先生决定让学生知道，独裁很容易实现。学生无法理解法西斯主义，但突然间，他们成了这场运动的一部分。教师Ron Jones大胆地进行了一场实验：他向他的班级灌输纪律性和集体精神。<br />
<br />
星期一<br />
教师Ron Jones要大家按位置座。现在开始，让大家变得有秩序和纪律，对老师使用敬称，纠正坐姿站姿，请求发言要举手，发言时要站立，不服从的被要求退出选课。如果不满意，可以退出。Ron Jones要告诉学生，服从、纪律、统一<br />
<br />
星期二<br />
Jones踏入教室，迎接他的是一片肃静。所有人在课桌后坐得笔直，虽然并没有人这么要求。他们的聚精会神，表情充满期待，没有人交头接耳。Jones在黑板上写下：“纪律铸造力量”-“团结铸造力量。<br />
<br />
星期三<br />
完全是有意或者无意的引导，学生们决定搞一种简单的制服：白衬衫和牛仔裤而已。制服可以做到什么：表明我们是一个团体，可以区分异己。并且他们起了个名字“浪潮”。在老师和学生的讨论中得出了这个结论。<br />
<br />
星期四<br />
一个觉得穿白衬衫太傻，同时也对这个运动有所不满的女孩最终穿了一身红色。今天班上只有她没有穿白衬衫，红衣女孩孤立几乎是瞬间的:她不属于这个集体。老师公开的无视，学生也不和她讨论，她感到了巨大的压力和孤立，就连她的男友也发出了怨言：你太不合群，太自以为是了。同时班上一个沉默寡言内向的孩子，在学校一直被欺负，当其他班的学生又来欺负他的时候，他开始了反抗，而他的同学，穿着同样制服的同学看见了，过来保护了他。在这场运动中，学生们感到力量和自我。<br />
<br />
星期五<br />
动议制定统一的手势，这不是Ron Jones想出来的，只是学生觉得好玩搞出来的，用这种与众不同的方式表达一下。有学生家长说Ron Jones的教育让学生焕然一新，学生更加精神了，是值得肯定的。<br />
<br />
星期六<br />
活动周最后一天，按照Ron Jones的预想说我们现在做的就是法西斯当年做的，并宣布独裁实验结束，要解散“浪潮”！叛徒！叛徒！群情激奋的学生要求制裁这个叛徒，没有预谋，没有煽动，只有对运动的忠诚和热爱，一切制裁都是合乎情理的。<br />
TIM站了出来，浪潮是他的第二生命，他绝不允许背叛，甚至Ron Jones也不成！在网上购了一只手枪的他阻止“浪潮”解散，开枪射击那些想要阻止“浪潮”的人。<br />
