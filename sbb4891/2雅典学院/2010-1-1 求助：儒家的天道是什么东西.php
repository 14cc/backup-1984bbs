													<h2>1988年11月27日刘晓波答记者问</h2>
						
						
																											<div id="postmessage_409891" class="t_msgfont">对孔孟之道的批判<br />
<br />
问：你曾宣称要彻底埋葬孔孟之道，在它的废墟上能建立现代中国文化，如果一个民族完全否定了她的传统文化、失去了基础，如何建设一个新文化?<br />
<br />
刘晓波：传统文化只是提供了一个否定的基础和起点，不是继承和承袭的基础。我认为孔子是个庸才，孟子比他有智慧，他承认的天才是庄子，从哲学上说，孔子什么都不是，孔子的学说是一种入世的为政治服务的学说，汉代把它变为统治工具，它的生命到汉代就该死亡了，奇怪的是这么多年还没有死亡!不过，它面对新世界已经死亡了。西方有人喜欢孔子，不奇怪，因为是多元社会，但是在一元化的社会中，最好的东西也没用。所以孔子在东西方的意义不一样，如果中国是个多元化的政体，我不反对别人信仰马克思、信仰基督教、信仰孔子，但当前的中国，你信仰马克思就等于信仰一种思想独裁，因为马克思主义在中国是统治阶级的工具，是棍子，不具有理论意义。<br />
<br />
问：有人认为亚洲四小龙经济上的成功是儒学的胜利，证明儒学还有现代价值。<br />
<br />
刘晓波：这是胡说八道!也是忘恩负义。台湾、南韩、新加坡都有美国支持，日本也是，如果没有美国的人权观念的约束，这些国家可能什么都没有!这是东方人的丑恶，东方人面临着人的解放的问题，中国是一架政治机器，日本是一架经济机器，每个人都是机器上的一个螺丝钉。台湾、日本的人权问题表示解决，香港解决了西方近代的个人主义是功利化的，它争取的是政治与经济的权利，但现代存在主义哲学追求的则是生命意义上的个性解入，这是一种“纯哲学”。<br />
<br />
问：哲学问题我们只能点到辄止了。不如你说说，你如此反传统，是否同意全盘西化?<br />
<br />
刘晓波：现代化是至明真理：私有制、民主政治、言论自由、法律至上。这是无可争议的，中国不存在理论问题，只有政策问题，全盘西化就是人化、现代化，选择西化就是要过人的生活，西化与中国制度的区别就是人与非人的区别，换言之，要过人的生活就要选择全盘西化，没有和稀泥及调和的余地。我把西化叫做国际化、世界化，因为只有西化，人性才能充分发挥，这不是一个民族的选择，而是人类的选择，所以，我很讨厌“民族性”这个词。中国就讲不清什么是“中国特色”。<br />
<br />
问：据了解，大陆思想界对你阁下的这些高见，颇有一些如“走极端”、“绝对化”的批评，你有何辩解?是否有意赋予它的特殊意义?<br />
<br />
刘晓波：不是，我的声音只属于我自己，那只是中国的一种声音，别人搞条理很清晰、论证很严密的学问，我不反对，但我的方式就是这样，无以名之，但并不妨碍我欣赏西方的科学哲学。它严密的论证环环相扣，但我不会那样写，我的极端应有存在的理由，我并不要求别人和我一样。我不喜欢钱钟书那样写一条后引好几十条考证式的文章，那是他的一绝，我承认他的价值，虽然我可能认为他很荒谬。<br />
<br />
问：你的思路和表达方式有鲜明的个人色彩，是怎样形成的?通过思辩、经验还是人生的独特背景?<br />
<br />
刘晓波：我向来不喜谈“我的治学之道”，没什么道，只不过我与别人不同，其他没什么好谈的。有的人动辄谈成材之路……他妈的，你有什么资格给青年人做楷模，当青年导师!我很欣赏无法之法，每个人只有自己的方法，无统一的方法，做学问做人皆如此。任何一个人的成功，都是别人的坟墓，不要走别人的路。</div>

							
							
							
							
