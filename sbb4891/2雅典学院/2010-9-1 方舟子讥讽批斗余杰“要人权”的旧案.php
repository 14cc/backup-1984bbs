另附：致中国作家协会的公开信 <br />
<br />
余杰 <br />
<br />
一份具备了无可辩驳的合法性的协议，居然被轻率地拒绝履行，这样的事情堂<br />
而皇之地发生在提倡“依法治国”的今天，让人觉得难以置信；一个在宪法所<br />
规定的范围内发挥公民创作自由的青年作家，居然被以“莫须有”的罪名剥夺<br />
工作的权利，这样的事情发生在巴金老人担任主席的中国作家协会，让人更是<br />
莫名惊诧。我不得不思考这样的问题：在今天的中国，究竟是“人”大还是<br />
“法”大？ <br />
今年年初，我在研究生毕业前夕，希望选择一家学术氛围良好的研究部门，毕<br />
业后从事文学研究工作。我联系了中国作家协会下属的中国现代文学馆，真正<br />
吸引我的是该馆保存着近、现代作家的手稿、藏书等大量珍贵资料。文学馆的<br />
三位领导――馆长舒乙先生、副馆长吴福辉先生和刘泽林先生，详细地阅读了<br />
我的履历材料并进行了面谈。他们对我在北大学习期间发表的数十篇学术论文<br />
表示欣赏，对我到文学馆从事现代文学研究工作的意愿表示欢迎。4月初，我<br />
与北京大学、中国现代文学馆以及文学馆的上级主管部门――中国作家协会，<br />
正式签订了一式三份的《全国毕业研究生就业协议书》。6月初，我与中国作<br />
家协会又签订了一式两份的《就业协议书》。至此，所有的协议文件均已齐<br />
备。 <br />
7月，我通过论文答辩，获得北京大学文学硕士的学位。按照正常手续，我持<br />
由中华人民共和国教育部印制、北京大学开出的《全国毕业研究生就业报到<br />
证》，去中国现代文学馆报到。但是，该馆突然不予报到，声称遵照中国作家<br />
协会领导指示，不再按照协议接收我。此后，我多次与现代文学馆和作家协会<br />
联系，但他们都不出示任何拒绝毕业生的理由――连“莫须有”的说法也不敢<br />
给出，却一直态度强硬，不让我报到。报到的最后日期是8月5日，这一日期过<br />
去将近两个月之后，我依然没有获得一个明确的说法，无法到现代文学馆展开<br />
我的研究工作。在此期间，作协人事部门态度粗暴而轻率，将其保存的一份<br />
《全国毕业研究生就业协议书》通过文学馆退还给我。他们无视协议中规定的<br />
有关条款和《劳动法》等法律法规，居然无知地认为：退还协议书，协议就不<br />
存在了、就作废了。这种蔑视法律、毫不负责的行为，让人难以理解。 <br />
由教育部高校学生司制定的《全国毕业研究生就业协议书》中，有如下规定：<br />
“三方中有任何一方提出不能履行协议，必须经另外两方同意并支付违约金<br />
（在学校的建议计划上报教育部之前违约，交违约金2000元；在学校的建议计<br />
划上报教育部之后，教育部下达就业计划之前违约，交违约金3000元；在教育<br />
部就业计划下达后违约，需报教育部和北京市教委批准，交违约金5000元）<br />
后，本协议可中止执行。”然而，作家协会不仅没有经北京大学和我本人同<br />
意、也没有支付违约金，居然突然袭击、单方面毁约，给我的正常生活造成严<br />
重的影响。此时，我已经错过了毕业生联系工作的时间，无法再联系其他单<br />
位。作为一个公民应当享有的的工作权利，被作协无法无天地剥夺了。 <br />
2000年9月28日下午4点，我与律师和几位朋友来到中国作家协会办公楼，希望<br />
与作协人事部门的负责人见面，听取该负责人对作协单方面违约的原因说明，<br />
并希望获得文字性的说明意见。作协人事处处长认为，今天我本人没有提前预<br />
约就找上门来，这一行为十分“不妥”。而我认为，作协在长达三个月的时间<br />
里不履行协议，不与当事人联系，无缘无故地将协议退还本人，这才是真正的<br />
“不妥”。今天我上门来交涉，完全是为了捍卫协议的合法性和个人的工作权<br />
利，是合情合理合法的，并无任何的“不妥”。该处长又认为，我应当与文学<br />
馆方面交涉，此事与作协无关。而我认为，《全国毕业研究生就业协议书》<br />
中，有作为甲方“毕业研究生”的我的签字，有作为乙方的“用人单位人事部<br />
门”的中国现代文学馆的签章和“用人单位上级主管部门”的中国作家协会的<br />
签章，以及作为丙方“培养单位”的北京大学的签章，我与作协都是地位平等<br />
的签约方。所以，我有权直接找与作协交涉。更何况我又与作家协会单独签署<br />
了《就业协议书》，作协必须承担此协议带来的相关法律责任，没有权利拒绝<br />
与另一签约方直接对话。 <br />
就在与该处长交谈的过程中，令我意想不到的事情发生了：周围几间办公室里<br />
冲出若干名工作人员，将我们团团围住。有两名年轻力壮的男工作人员对我们<br />
进行粗鲁的辱骂，其中一人甚至冲上前来，企图动手打人，在尚未得逞之前被<br />
另一名女工作人员拉开。 <br />
如此野蛮的言行居然发生在堂堂作协大楼之中，让我对这一机构的本质有了更<br />
加清醒的认识。我险些以为自己身处某黑社会总部的大楼之中，连基本的生命<br />
安全也得不到保障。 <br />
据知情人士告诉我，我被作协拒之于门外，是因为我的某些文章惹的祸。我不<br />
知道这是不是问题的症结所在，但我认为：一个公民在宪法和法律允许的范围<br />
内，有思考、言论和写作的自由。我的论文和文章都发表在国家公开出版的报<br />
刊杂志上，我的著作也全都是由国家正式的出版社出版发行的。在法律的意义<br />
上，我毫不畏惧地对自己的每一篇文章、每一个字负责。对于一篇文章、一本<br />
著作，作为读者（包括某些官员在内），当然可以智者见智、仁者见仁。作为<br />
作者，我也会虚心地倾听来自各方面的批评意见（自然包括某些官员的批评意<br />
见）。当然，经过我的独立思考之后，是否接受这些意见，同样是我的自由。<br />
如果作协的有关领导对我写作的立场和文章中具体的观点有不同看法，可以直<br />
接找我沟通和交流，也可以通过其他途径向我转达。如果认定我的哪篇文章、<br />
哪个观点违反现行法律法规，甚至有“政治倾向问题”，也完全可以在公开场<br />
合指出和批判。但是，中国作家协会既没有私下与我交换意见，也没有公开宣<br />
布我的文字存在着什么程度的问题，却采取了最等而下之的办法：全然是幕后<br />
黑箱操作，企图通过剥夺个人工作权利的方法来压制不同的声音。这种做法，<br />
与浩浩荡荡的世界民主化潮流背道而驰。 <br />
我在北大从本科到研究生度过了长达八年的学习生涯（包括为期一年的军政训<br />
练）。在此期间，我一直勤奋思考、勤奋写作，出版六本著作（总数达二百多<br />
万字），并在第一流的学术刊物上发表论文数十篇，引起学术界的好评与重<br />
视。我的硕士论文《〖知新报〗研究》，受到由孙静教授、温儒敏教授、陈平<br />
原教授、刘勇强教授和吴福辉研究员组成的答辩委员会的高度评价。答辩委员<br />
会指出：“该论文具有弥补学界薄弱环节的价值，且为报刊个案研究提供了一<br />
个成功的范例，是一篇优秀的硕士论文。”作为一名优秀的北京大学2000届毕<br />
业研究生，居然无端被签约单位拒绝接收，毕业后无法从事正常的研究工作，<br />
这既是对中国最高学府――北京大学的侮辱，也是对国家人才培养计划的嘲<br />
笑。国家投入巨资培养的一名优秀人才，却被作协某些领导单凭一己之私见拒<br />
之于门外，这也是对国家财产的肆意浪费。在国家领导人强调不拘一格地为青<br />
年的成长提供良好的外部环境的今天，作协的某些领导却以压制、打击人才为<br />
荣，让人百思不得其解：中国作家协会的性质，究竟是为作家提供帮助和服<br />
务，还是破坏作家的创作自由？ <br />
联合国《世界人权宣言》郑重指出：“人人有权工作、自由选择职业、享受公<br />
正和合适的工作条件并享受免于失业保障。”（第二十二条第一款）中国政府<br />
已经正式签署的《经济、社会及文化权利国际公约》中也明确规定：“本公约<br />
缔约各国承认工作权，包括人人应有机会凭其自由选择和接受的工作来谋生的<br />
权利，并采取适当的步骤来保障这一权利。”（第三部分第六条）作为一名履<br />
历清白、学业优秀的北大毕业生，我所要求的仅仅是协议所赋予我的工作的权<br />
利，这也是中华人民共和国宪法和法律、联合国《世界人权宣言》和《经济、<br />
社会及文化权利国际公约》等权威文件所保障的公民的合法权利。 <br />
我深知，在某些特定的历史时期，作为一名思考者、言说者和写作者，需要为<br />
自己的观点付出特殊的代价。我有这样的思想准备，也不回避任何不公正待遇<br />
的来临。即使失去了应得的工作，我也可以依靠自己的写作在自己的祖国生存<br />
下去，尽管生存会变得艰难一些。我深爱这片苦难的土地和生活在它之上的苦<br />
难的人民。我为自己而写作，也为苦难的土地和苦难的人民而写作。 <br />
同时，作为一个现代社会的公民，作为一个强调“民主和法治”的国家的公<br />
民，我更要捍卫自己天赋的权利。我相信，在我的背后还有法律、正义和良<br />
知。我不愿意像我的长辈一样，在不公正的待遇降临的时候，只会屈辱地承<br />
受、默默地忍耐。我将拿起法律的武器、运用舆论的力量，以一种相当理性的<br />
方式，为我自己――一个普通公民的权利而奋斗。其实，这也是为每一位遭到<br />
不公正待遇的知识分子的命运而奋斗和抗争。抗争的结果对我来说并不重要，<br />
更重要的是抗争的过程本身。 <br />
我生活过，写作过，抗争过，这就够了。 <br />
从这一刻起，感到恐惧的将不是我，而是那些躲藏在黑暗的角落里的家伙。 <br />
从这一刻起，我让自己沐浴在灿烂的阳光之下。我将生活得更加充实和快乐、<br />
更加坚强和自信。我将不畏惧任何的伤害。 <br />
<br />
2000年10月1日深夜<br />
