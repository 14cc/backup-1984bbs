																											<div id="postmessage_807767" class="t_msgfont">方舟子：既然作者（余杰）请求各大网站代为广为张贴，咱就自命“大”网站，代他贴一下。<br />
但我对余杰的这种作法却不以为然。既然相信“依法治国”，就应该通过正常<br />
的法律途径解决。如果《就业协议书》真有法律效力，毁约真的违背了劳动法，<br />
那就到法院控告中国作协就是了。作协不让进，也还有别的地方可去。这跟什<br />
么人权扯得上吗？“作协某些领导”，甚至任何机构的领导，都没有给你劳动<br />
权的义务。在法律上，只是毁约的问题。如果告了状，而司法不公，或者被政<br />
府禁止到别的任何机构工作，甚至自谋生路也成问题，再来大谈人权、劳动权。<br />
一受委屈，就大散冤枉状，上纲上线，让人觉得其意不在毁约一事。余杰不是<br />
自命鲁迅的私淑弟子吗？可记得当年鲁爷因支持学潮被教育部“某些领导”免<br />
掉了教育部佥事一职时，并不是大散公开信，而是告上了法庭并获胜？<br />
（方舟子）<br />
<br />
提醒余杰类：个案就是个案<br />
（前几天我还在感叹国人往往分不清“权利”（right）和“特权”（privilege），<br />
这又是一个现成的例子，而出自于以启蒙中国民众为己任的“自由主义者”笔下，就<br />
更为可悲。小余不明白，作协毁约，并不等于他“在一夜之间莫名其妙地丧失了工作<br />
的权利”，除非他因此没办法在中国的任一机构工作或自谋生路。到作协工作是一种<br />
特权，而不是权利。同样，“丧失了在北京落户口的权利”，那又是什么“权利”？<br />
中国有多少人有这样的“权利”？那也是一种“特权”。对“特权”的丧失就别自顾<br />
自怜了。<br />
“人权”何时成了贬义词？“人权”有特定的含义。人权是人的基本权利，并不包括<br />
“进作协工作”、“在北京落户”这样的特权。大家反感小余为了一份协议大谈人权，<br />
是因为从法律上看这事和人权一点关系也没有，即使立志要将“人权观念根植在每个<br />
公民的心灵深处”，也不等于你可以将自身什么鸡零狗碎全往人权问题上扯。莫非小<br />
余将自己看成了人权的化身（他所谓替“沉默的大多数”说出了几句心里话），凡是<br />
自身的利益受侵害，就都是跟人权有关了？那样的话，“人权”倒有可能要变成贬义<br />
词了。还能扯上“被警察抓到昌平去挖沙子的人”、“犹太人”等等，大义凛然的样<br />
子实在滑稽得可以，真不知从哪里学来的如此大幅度的跳跃性煽情思维。至于余杰声<br />
称本该得到60万元的稿费结果只得到了1万元，那恐怕是书商的责任，与其向大家哭<br />
穷、要人权，不如向书商讨还公道维护自己的权益。<br />
（方舟子）<br />
