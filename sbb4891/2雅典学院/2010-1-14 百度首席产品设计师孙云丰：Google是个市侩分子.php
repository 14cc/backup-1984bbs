利益与伦理：再谈google和百度 <br />
兰小欢 @ 2010-1-14 7:42 阅读(8998) 评论(46) 推荐值(541) 引用通告 分类: 未归类 <br />
<br />
有几位朋友在博客或者twitter上都留言提醒我注意google事件的“商业道德或伦理”，不能只用利益来看问题。这里我来谈谈这个问题，并就此说说我对百度孙云丰文章的看法。<br />
<br />
我上文开篇说的清楚：“认为google讲道义，这种态度是一厢情愿，有戏剧效果，但分析上走不远。”我当然承认有商业道德这回事，但我说的是这分析走不远。如果用商业道德为分析起点，那要解释的现象是google和百度为什么行为不同，分析的原因最后是商业道德不同，或者再加一层“判断”：google就是刚正不阿，百度就是奸佞小人。<br />
<br />
这分析框架原则上没问题，但选择这个角度，许多重要的问题解释起来会很困难，比如为什么google“好”百度“坏”？为什么google时坏时好？要用道德为起点回答这类问题，分析基本上逃不出同义反复或者套套逻辑。而如果你认为商业伦理归根结底是人的道德问题，那如果把百度和中国谷歌的所有员工对调一下，两家公司行为会有不同么？<br />
<br />
基于这些困难，我选择“利益”来看问题，这是经济学的分析。选择一个角度切入不等于否认其他角度的存在，而是一个理论的取舍：坚持一个基本出发点才可能逻辑一致地想下去，否则一会儿这样一会儿那样，就不能称之为分析了。更重要的是，选择“利益”做角度不仅能帮助回答以上三个我认为很重要的问题，而且“利益”的角度包含了“商业伦理”，选了前者，我不需要再考虑后者也不会遗漏重要的分析结果。最后，相对于商业伦理，长期和短期利益都更容易观测和估计，不一定观测和估计的准，但可以观测和估计（股价就是干这个的）。这是个优势，因为可以据此回答更多的问题，比如“利益变动多少行为会变化多大”，但“道德高低多少行为区别多大”这问题就很难回答。<br />
<br />
1.&nbsp; &nbsp;1. 为什么google“好”百度“坏”呢？<br />
<br />
因为中文业务对google来讲远远不是生命线，现在不是，将来也不会是，所以它有选择的本钱；而中文业务几乎是百度的全部，是它的生死存亡问题，二者行为自然有别。我不认为google就比百度更道德，都是利益至上的商业机构，二者面临的局限不同所以行为不同罢了。<br />
<br />
如何回应百度孙云丰的质疑“如果谷歌占据了中国80%的搜索市场份额，google的高管，还会这么高调的宣称要do no evil，从中国退出吗？”还是那句话，就算占据中国80%的市场份额，中文业务也只是google全球业务的一部分，不是全部，它永远有根据全球利益来调整中国市场上行为的能力，而这能力，要比百度强很多。如果中国的业务是google全球业务的80%以上，那google的行为就会和百度趋同。<br />
<br />
所以在这个问题上，孙云丰有道理。但这句话应该引发更多的思考：1）为什么google在中国的利润空间有限？当然，百度的竞争是一方面，在这方面百度做的不错，但也不要由此就狐假虎威起来。如果没有强大如幽灵般的宣宣，我想google断不至于因为百度的竞争而考虑放弃中国这个巨大的市场。2）为什么google在中国的业务可能和它的全球盈利起冲突？宣宣只是一方面，毕竟宣宣大约也不管你卖“搜索排名”，也不直接插手google book的版权问题。更多的经济政治因素造成google在中国的困境，值得深思。<br />
<br />
我也不认为google比思科等公司更“道德”，只是google的业务范围或者赚钱手段更明确地依赖于信息自由流动，在这一点上和当前的环境起了大冲突，所以看上去就有些“斗士”和悲情的意味。再加上google声明书中揭露的某些细节，这意味更浓。正所谓时事造英雄。我们可以想象一下，如果帕玛拉特牛奶不是在2005年退出中国市场，而是在三氯氰胺事件爆发时离开，那个转身恐怕也会相当华丽和高洁。但其实谁离开都是因为无利可图，成本太高，利润前景不明。只是时局不同，我们不明真相的群众感受便不同。<br />
<br />
说过很多遍了，比起这些道德感受，事件背后的经济政治力量变化会带来更多实际的变化，这是我关注的问题。<br />
<br />
2.&nbsp;&nbsp;2. 为什么google时“好”时“坏”呢？<br />
<br />
google还是那个google，人还是那群人，口号也还是那个口号，伦理没变，环境变了。你可以形象地说当局太过分了，连老实人都忍不了了。但是别忘了，当局在欺负人这件事上，通常是一视同仁的，欺负google的同时不会忘了百度，但由于二人抵抗力不同或者成本收益结构不同，反应不一样罢了。<br />
<br />
你可以嘲笑百度，比如没有“鸡吧”等等，我也常常嘲讽它。但当我们认真思考的时候，问题就不是谁可笑，而是为什么百度更可笑。<br />
<br />
是因为百度从上到下，总得来说人品有问题或者“软骨头”特别多么？当然不是的。把这茬人都放google去，一样大义凛然，因为中国业务不再会伤筋动骨。所以二者的差别还是业务和利益的差别。<br />
<br />
你当然可以继续赞扬google谴责百度，说百度也应该当英雄不当太监，最差还可以选择倒闭，选择不为虎作伥。你这么骂政府是没问题的，因为政府强制纳了你的税收了你的钱，你天经地义的可以骂它。但外推到别的事物身上，要多想一想是不是苛责了。<br />
<br />
3.&nbsp; &nbsp;3. 个人道德和商业伦理<br />
<br />
有位MBA的朋友留言说“商业伦理是个人道德的最大公约数”，这句话我特别喜欢，一个“最大公约数”道尽了“妥协的艺术”。个人的道德观念不能简单外推到集体行为，本质的原因就是因为集体行为是各种单个利益和力量角逐和妥协的结果，这角逐和妥协结果不是简单的个人观念的加加减减（“简单外推”）。<br />
<br />
单个的人，可以意气用事怒发冲冠壮士断臂玉石俱焚，但集体行为通常会减少这类极端情况（在有些情况下有可能加强变得更极端，典型的比如战争），因为要照顾各方面的利益。以商业公司看，大家是为了利益结合在一起，“逐利”是结合的根本，利益虽不等于商业道德，但商业道德比如诚信合作等根源还是长期利益。没有利益，这公司根本不会存在，商业道德也就无从谈起了，这是公司和个体的重要差别。所以“壮士断腕”这种事发生在商业世界时一定是原因的，或者断腕能换来一条腿（以退为进或丢车保帅），或断的是别人的腕，或者你以为是“断腕”其实不过是“拔毛”，像google这样。<br />
<br />
<br />
<br />
<br />
4. 4. 孙云丰的困难<br />
<br />
百度孙云丰的文章我看了，有几个地方非常不解。<br />
<br />
第一、&nbsp;&nbsp;在商言商，大家都是为了赚钱，而一家商业公司的高管批评另一家商业公司的理由竟然是“市侩”，这让我很无语。google摆个Pose就“市侩”，百度天天屏蔽就不“市侩”了？至于用什么借口，都是老商人了，何必当回事。Google“拿一个高管制国家的民众感情来做台阶”就是“极其不道德”，其他包括百度天天喊着“保护民族工业”然后抬高价格朝民众兜里猛捞钱就“道德”了？都是老江湖了，用“恶心”来撒娇，不合适。<br />
<br />
第二、&nbsp;&nbsp;他又说搜索引擎最大的社会政治意义是为老百姓提供透明的信息。反思自己的人生和意义，没问题，但之后的说法就太离谱了：“从这个角度而言，尽可能的设法为百姓提供便捷的信息获取技术服务，提供切实的价值，而不是挂羊头卖狗肉的宣称自己do no evil和政府撕破脸皮搞壮烈，才是一种真切的负责态度。”<br />
<br />
人家是来赚钱的，你也是来赚钱的，“切实的价值”就是真金白银，扯那么远，怎么听都不像是诚实的商人，而像不诚实的“三个代表”。政治家应该为选民负责，公司应该为股东负责，这才是“一种真切的负责态度”。扯什么老百姓啊，人家又没强制纳老百姓的税，和老百姓是平等交换关系，对老百姓有个鸟责任。是不是受压迫太久了，看谁都像是救世主？幻觉破灭了就说人家不是好人？<br />
<br />
第三、&nbsp;&nbsp;通篇都是这样的胡话，不多说了。在商言商有事儿说事儿，别老占据道德制高点，又不是竞选总统。<br />
<br />
<br />
<br />
<br />
====================================<br />
<br />
最后，我以为世界上最文明的事就是在重视彼此利益基础上的相互妥协，对抗是为了利益，谈判也是。重视利益没有贬低道德的作用，只是换了个说法，在现象上，“道德规则”的背后永远有利益，无论用传统的经济学分析社会现象，还是用更新的进化论心理学分析个体行为；无论分析的单位是经济学的“自利的个人”，还是进化论和生物学的“自私的基因”，都会引致类似的结论。<br />
<br />
如果你觉得讲利益不谈道德让人感觉不舒服，那我还可以讲讲更让人不舒服的，比如爱情中女人的直觉，还有一见钟情命中注定意乱情迷。这种种神秘和浪漫，根据2009年最新的女性性（进化论）心理学的集大成之作《why women have sex》,都有极度不浪漫不神秘的进化论和基因解释，那解释和种种实验结果，说实话比经济学“自私自利”多了。但转念一想，正如黄舒骏唱的“虽然结果颇令人伤心，了解之后也没什么了不起。” 其实只会让你更赞叹世界的丰富，有规律的层次井然的丰富。</div>

							
							
							
							
