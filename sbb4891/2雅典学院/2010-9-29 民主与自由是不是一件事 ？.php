																											<div id="postmessage_849561" class="t_msgfont">民主与自由本来就是两回事.<br />
<br />
民主就像是一把菜刀,你是把它放在厨房里方便你的生活,还是把它拿到大街上去砍人,取决于你的选择.在成熟的公民社会,这样的选择通常都是理性的,所以民主这把菜刀自然就进了厨房,没有对社会产生危害.而在不成熟的社会或者种族矛盾严重的社会,人们的不理性往往多于理,在这样的社会环境里,民主这把菜刀通常就在大街上飞舞了.<br />
<br />
我的理解是,民主其实就是一个工具,这个工具是用来解决社会公共事务的,而且民主这个工具也能够在一定程度上抑制个人专制或者政党专制的出现.一个社会在使用这个工具的时候,必然会出现&quot;多数人&quot;与&quot;少数人&quot;之间的矛盾,但是说穿了,也是人民内部矛盾.你今天在某个公共问题上可能是多数人,但你明天在另一个公共问题上可能就是少数人了,这种切换其实是民主社会的常态.最重要的是,个人权利(自由)能不能在民主社会里得到保障?如果不能,那么这种民主社会其实与一人独裁或一党专制没什么区别.所以,衡量一个民主国家是不是货真价实的,就必须去看这个国家的少数人的权利有没有得到保障.(比如同性恋,少数民族,小教派等等),这也是检验一个国家文明程度的必要参数.<br />
<br />
另外必须说明的是,人们在使用民主这个工具的时候,都会举起&quot;公共利益&quot;的大旗.这面旗帜在中国是很吃得开的,因为我们从小接受的教育就是牺牲小我,成就大我.为了多数人的利益而牺牲少数人甚至个人利益,在我们的价值观念里是可以接受的,甚至还会认为这是光荣的,崇高的.而在真正地民主国家里,是不承认这种做法的.不过,总会出现公共利益与个人利益相冲突的时候,比如拆迁.在美国著名的凯洛诉新伦敦市一案中,就是这种利益冲突的直接表现.我在这里摘抄一段相关文章.<br />
------------------------------------------------------<br />
“公用”与“合理补偿”原则<br />
<br />
此案中的原告宣称自己的财产为“非卖品”，因此不涉及征用补偿金是否合理，而是涉及“国家征用权”中的“公用”概念。<br />
<br />
这个案子的特别，是新伦敦市政府征用土地的“公用”目的，不像修公路铁路那样清楚无疑，它的征用目的是“发展城市经济”、包括商业区在内的新区开发，形式上就像一般的商业开发。由于政府本身不拥有开发企业，没有国营州营公司，市政开发项目必须委托私人开发公司完成。人们质疑的是，它是不是由政府的开发计划来把土地的使用权从原主手里转到其他私人的手里。<br />
<br />
联邦最高法院注意到，在康州法院的审理过程中，法官们虽然对裁决有分歧，但是都确定，在这个案子里，没有任何违法行为。在这个案子里，联邦最高法院主要是审核：一、政府征用特定的土地是否确属城市合理、必须的发展，并且符合“公用”这一要求；二、征用的土地是否确属合理的预期发展计划，也就是有没有过度征用。联邦最高法院并不重新审视规划细节，那是下级法院的事情，联邦最高法院是裁定前面法院的判决是否违宪。<br />
<br />
结果，联邦最高法院以五比四裁决，维护了康涅狄格州最高法院的裁决，认为新伦敦市的征用土地计划，符合“公用”的法律要求，只要满足宪法第五修正案的要求，对原主作出合理的补偿，这一计划就符合“国家征用权”的标准，康涅狄格州政府和新伦敦市政府动用“国家征用权”就是合法的。<br />
<br />
联邦最高法院的裁决只有一票之差，说明这是一个有重大争议的裁决，其原因在于此案的“公用”概念在具体实施中，必须有私人开发商的参与。这就产生了是否会损害民众私人利益而让大企业获利的疑问。联邦最高法院著名女大法官欧康诺表示反对，并在最高法院意见书后附加了篇幅更长的反对意见，首席大法官兰奎斯特等另外三位大法官附议支持了欧康诺大法官的反对意见。<br />
<br />
《纽约时报》刊登了来自民间的不同意见。支持者认为，这一裁决保障“公用”土地，维护了公众的利益；反对者担心，以后地方政府是否可以利用这个案子的判决，以政府行为滥用“国家征用权”。其实，最高法院并没有说以后凡是商业开发，政府都可以征地。它是针对新伦敦市的案情，确认政府以协助商业开发的形式，来推动社区的公共利益，可以算是符合“公用”的法律要求，从而是可以动用“国家征用权”这一概念的。至于作出合理补偿，并且在征地、招标、发包等开发过程中实行公开透明的程序，符合现有法律的约束，这几乎是不言而喻的要求。<br />
<br />
此案的裁决，只是联邦最高法院再次重申了动用“国家征用权”，必须以“公用”和“合理补偿”为原则，只有这样才不致破坏国家对私有财产权的保护。五比四裁决传达了最高法院的谨慎，因为对“国家征用权”的适用范围作出一次新的解释，可能是一件非常危险的事情。一票之差表明，大法官们都意识到，“国家征用权”的滥用，会侵犯私人财产，破坏这个国家作为基础的私有财产制度。<br />
-------------------------------------------------------<br />
<br />
总的来说,民主与自由之间,自由与安全之间,自由与平等之间,永远是一个不断平衡的过程,最重要的是,在平衡的过程中,各方能不能够遵守法律,用理性和妥协精神参与进来,调和彼此之间的不同.假如中国社会明天就民主了,但能不能做到这一点,我持悲观态度.换句话说,我们就算把民主这个工具拿在手里,能不能用好它,还是个大大的问号.所以,我们把民主捏在手里以后,我们每个人能不能得到自由,同样也是一个大大的问号.</div>

							
							
							
							
