													<h2>民主和自由是两回事</h2>
						
						
																											<div id="postmessage_849547" class="t_msgfont">自由：首先，自由仅仅限于政治哲学里面的自由，也就是哈耶克所言“免于来自人的强制”。其次，这个免于强制是对等的，你希望免于什么样的强制，你就一定不能以这种方式强制他人。彼此能够达到关于这个“强制”的共识，大家就实现了政治自由。<br />
<br />
民主：民主是社群公权力的一种最高来源，没有社群公权力，谈不上民主。社群公权力还有其他来源，比如血缘-皇族血统；还有信仰-宗教；还有武力-殖民统治；不一而足。民主则是目前大家最认可的一种，每个人都平等地拥有公权力。当然，在执行过程中这些公权力可以做有限的转让，比如选举，选举就是把自己的一部分公权力在一定期限内转让给某个候选人。<br />
<br />
民主可以侵害自由，这就是所谓的多数人暴政。自由是关于“人的强制”，这里的人，既可以是个人，也可以是群体。如前所述，比如我不反对同性恋，别人就不应该用这个理由反对我，可是，作为一个群体，可能就以这个理由来限制我，这就违背了自由的原则。<br />
<br />
好的社会模式是民主和自由的结合，需要一个界限：界限以内是个人领域，不能超过这个界限来侵害个人自由；而在这个界限外的公共事务，以民主的方式来决定。这就是宪政民主，目前最好的政治模式。<br />
<br />
这个模式的难题有两个，第一是这个界限的位置，第二，以何种形式保障民主在公共领域被实现。</div>

							
							
							
							
