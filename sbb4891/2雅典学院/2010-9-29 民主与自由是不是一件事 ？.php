																											<div id="postmessage_849491" class="t_msgfont">自由一词是生活中常用词汇；而真正要领会其内涵却很有学问；<br />
<br />
<br />
F.A.哈耶克 ：《自由秩序原理》<br />
<br />
<a href="http://bbs.morbell.com/viewthread.php?tid=247834&amp;extra=page%3D1" target="_blank">http://bbs.morbell.com/viewthrea ... &amp;extra=page%3D1</a><br />
<br />
1．本书乃是对一种人的状态（condition）的探究；在此状态中，一些人对另一些人所施以的强制（coercion），在社会中被减至最小可能之限度。在本书中，我们将把此一状态称之为自由（liberty or freedom）的状态。由于liberty 和freedom 这两个术语亦一直被用以指称人类生活中的许多其他善美物事，因此，开篇就追问这两个术语的真切意义，显然无益。如果首先陈述我在使用这两个术语时所意指的状态，然后在更为明确地界定我所采用的概念的时候，再来考虑这两个术语的其他意义，似乎更妥。<br />
　　一个人不受制于另一人或另一些人因专断意志而产生的强制的状态，亦常被称为“个人”自由（individual freedom）或“人身”自由（personal freedom）的状态；然而，我想提醒读者注意，如果我在这一意义上使用“自由”一词时，我将直接采用“个人自由”或“人身自由”的表达方式。有时， “公民自由”（civil liberty）一术语亦被用来表达与此相同的状态，但我还是决定不采用此一表达法，因为它太容易与所谓的“政治自由”（political liberty）相混淆；这两个术语间的混淆之所以不可避免，乃是因为“公民的”（civil）和“政治的”（political）两词虽一源出于拉丁文，另一源出于希腊文，但两词实具有相同的意义。<br />
　　透过上文对“自由”的含义所做的粗略界定，业已表明它所意指的乃是一种生活于社会中的人可能希望尽力趋近但却很难期望完全实现的状态。因此，自由政策（a policy of freedom）的使命就必须是将强制或其恶果减至最小限度，纵使不能将其完全消灭。<br />
　　我所采用的自由的含义，恰似该词的原始意义。人，或至少是欧洲人，一跨入历史便被归为自由的与不自由的两类；而且此一类分有着极为明确的涵义。尽管自由人间的自由可能有着很大的不同，但是这只是他们在独立程度方面的不同，而奴隶却根本没有独立可言。自由意味着始终存在着一个人按其自己的决定和计划行事的可能性；此一状态与一人必须屈从于另一人的意志（他凭藉专断决定可以强制他人以某种具体方式作为或不作为）的状态适成对照。经常用以描述这种自由状态的古老的说法，因而亦就是“独立于他人的专断意志”（independence of the arbitrary Will of an other）。<br />
　　“自由”所具有的这一最原始的意义，有时被说成是它所含有的平常或粗浅的意义；但是，当我们考虑到哲学家因试图精化或改进此一意义而导致的种种混淆时，我们不妨还是采用其原始的意义为佳。更为重要的是，这种意义不仅是自由的原始意义，而且还具有明确无误的品格，它描述的是一种状态，而且亦只描述一种状态；这种状态之为可欲的原因，则与我们欲求其他也被称之为“自由”状态的原因不尽相同。读者将在下文看到，从严格意义上讲，这些各不相同的“自由”并非同一类的不同变异形式，而原本就是完全不同的状态，且往往彼此冲突，从而应当将它们明确区别视之。尽管在其他意义上讲，人们也有理由说自由有着不同的种类，例如“免于（或摆脱）……的自由”（freedom from）和“做……的自由”（freedoms to），但在本书的讨论中，“自由”只有一种，其差别不在种类而在程度。<br />
　　就此一意义言，“自由”仅指涉人与他人间的关系，对自由的侵犯亦仅来自人的强制。这尤其意味着，人于某一特定时间所能选择的各种物理可能性（physical possibilities）的范围大小，与自由并无直接的相关性。一个陷于困境的攀登者，虽说只看到一种方法能救其生命，但他此时无疑是自由的，尽管我们很难说他是有选择的。此外，人们如果看到此攀登者跌入深渊而无力脱困，那么我们虽然可以在比喻的意义上称其为“不自由”，但大多数人仍在很大程度上认为其状态中间存有着“自由”（freedom）一词的原始含义；说他被“剥夺了自由”或被“因而丧失了自由”，其意义与它们被适用于社会关系时的意义极不相同。</div>

							
							
							
							
