													<h2>经常被人们误解的“苏格拉底之死”</h2>
						
						
																											<div id="postmessage_439371" class="t_msgfont">下面是我原先写的一篇博文。苏格拉底之死，是犯了民主恐惧症的人常提及的事，其实不过尔尔。<br />
<br />
　　“苏格拉底之死”事件经常被反民主人士用来诋毁民主，他们以此把“民主制度”等同于“群众暴政”。它还经常被中了犹太-基督教的毒而不自知的民主人士用来诋毁古代民主，认为古代民主不保护个人自由。前者的荒谬自不待言，后者也一样没道理。<br />
　　本文就仔细论述这件事的前因后果。<br />
<br />
　　下文引自罗素的《西方哲学史》<br />
&lt;&lt;&lt;&lt;&lt;&lt;<br />
　　苏格拉底受审的主要事实是无容置疑的。判决所根据的罪状是：“苏格拉底是一个作恶者，是一个怪异的人，他窥探天上地下的事物；把坏的说成是好的，并且以这一切去教导别人。”对他仇视的真正理由――差不多可以肯定地说――乃是人们认为他和贵族派有勾结；他的学生大部分都是属于贵族派的，而且其中当权的几个已经证明是极有危害性的。然而，由于大赦的缘故，这种理由便不能公开提出来了。法庭大多数都判决他有罪，这时按照雅典的法律，他可以要求某种较死刑为轻的处罚。法官们如果认为被告有罪的话，他们就必须在判决的定谳和被告方面所要求的惩罚两者之间作出选择。因此，若能提出一种法庭认为适宜而可以加以接受的相当重的处罚的话，那是会对苏格拉底有利的。然而他提出来的却是处以三十个米尼的罚金，这笔罚金，他的几个朋友（包括柏拉图在内）都愿意为他担保。这种处分是太轻了，以至于法庭大为恼怒，于是便以比判决他有罪时更大的多数判决他死刑。他无疑地是预见到了这种结局的。显然他也并不想以看来是承认自己有罪的让步，来避免死刑。<br />
&gt;&gt;&gt;&gt;&gt;&gt;<br />
<br />
　　苏格拉底是在三十僭主的邪恶统治被雅典人民推翻以后，民主派执行“不报复”政策时被处决的，这就是罗素所谓“乃是人们认为他和贵族派有勾结”的理由。要真是报复的话，应该还报复不到苏格拉底头上，但要是“不报复”的话，被强压下去的怒火就难免要想办法发泄到比较方便的发泄对象头上了。<br />
　　苏格拉底和三十僭主（贵族派）的关系有多深呢？<br />
　　从“法律”意义上讲，根本就没关系，苏格拉底是高傲的，僭主也是高傲的，他们之间怎么可能合作？实际上，苏格拉底是刻意避开过与后者的关系的。<br />
　　从“事实”意义上讲，关系可就大了。苏格拉底不但教育出一些王八蛋学生，除了上文所指的“已经被证明是极有危害性的”贵族派，还有第一个发明“乌托邦”思想以至贻害千古的柏拉图。而且他的很多思想也对民主不利。例如（仍然引自《西方哲学史》）：<br />
&lt;&lt;&lt;&lt;&lt;&lt;<br />
　　苏格拉底会问这样的问题：“如果我想修鞋，我要去找谁呢？”对这个问题，一些坦率的青年就回答说：“去找鞋匠啊，苏格拉底。”苏格拉底又会提到木匠、铜匠等等，于是最后便问到这样的问题：“谁应该来修理国家这只船呢？”当他与三十僭主发生冲突的时候，三十僭主的领袖，那个曾向他求过学并熟知他的方法的克利提斯，便禁止他继续教导青年们，而且还对他说：“不用再讲你那套鞋匠、木匠和铜匠了。由于你反复不休地提他们，现在他们已经被你讲烂了”<br />
&gt;&gt;&gt;&gt;&gt;&gt;<br />
　　在当时，这种言论是反民主的，因为希腊民主没有公务员制度，从各方面都是反对“专业人才治国”的。可笑的是，僭主也不买账。这个并不奇怪，在独裁者上台以前尽力诋毁旧制度，向独裁者抛媚眼的人，等到独裁者上台以后，都很少有好下场*。苏格拉底没有当时就被处决，只能是算他便宜，也是整个世界的不幸。<br />
<br />
　　话说到这儿，也许有人说了：就算苏格拉底这个人影响极坏吧，也不能就加以处决啊！<br />
　　这个话要分两头说：<br />
　　一、民主派根本就没打算一定处决苏格拉底。<br />
　　人们如果非要宰了某个人，自有稳妥可靠的方法。但在希腊-罗马的“异教”时期，对于“思想犯”“宗教犯”，从开始审理到最后行刑一般都会留出一些空间，让被告有机会悔过或逃跑。很多基督徒津津乐道的“迫害基督徒”案件，实际上也仅仅是希望基督徒改宗：只要改宗，祭拜一下旧时代诸神的神坛，案子也就撤销了，再没有任何其它惩罚措施。这和中世纪基督徒大规模屠杀异端是有本质区别的。<br />
　　苏格拉底有各种避免死刑的办法，但是他自己想死啊。他都70岁了，反正也活不了几天了，对他这么高傲的人物而言，早死几天比起“认错”来恐怕也算不上损失。<br />
　　倒霉的是民主派，他们大概只是想报复一下苏格拉底，出口恶气，没想到碰上这么个货色，弄得名声扫地几千年。<br />
<br />
　　二、虽然说没打算一定处决，但是判死刑仍然是过分了。不过，话说回来，这跟民主制度或古代民主制度有关吗？<br />
　　没有。这其实是个司法制度的问题。司法制度向来是有两种：法官判决制度和“普通人”判决制度。<br />
　　大体上讲，法官德高望重，法律知识丰富，地位和人身安全也有保障，一般不会瞎判。如果弄到法官都要“被迫瞎判”的情况，这个国家也快完蛋了；如果法官“自愿瞎判”，那就纯粹是个人责任，也好推卸出去...这就是法官判决制度的好处和“好处”，也就是说：一般不会出问题，万一出了问题...也可以说“这不是司法制度问题”。<br />
　　由普通人进行司法判决，包括美国的陪审团制度和把苏格拉底给宰了的这个制度。它的坏处之一就是：如果“人民”要公报私仇，你是一点办法也没有，因为法官还要考虑他一辈子的职业清誉，普通人才没这个顾虑呢。美国人有一套选择陪审员而防止出现“公报私仇”的办法，但一般只能防止“个别人”，也没办法防止“大多数人”，因为很难找到一套客观有效且无法规避的标准，把大多数人从陪审员中剔除出去。<br />
　　美国有很长一段历史时期，3K党无恶不做，就是因为陪审团制度的结果，几乎无一能判......我的话说到这儿，狡猾的人可能会说了“既然没判，那就是没罪呗，你凭什么说制度有问题？你是神吗？”<br />
　　呵呵，后来，美国国会就出来当“神”了，规定如果涉及到此类问题，陪审团明显枉法判决的，可以要求重审。<br />
　　这个规定当然有用，但是，“陪审团制度”不可靠也就显而易见了。而且，谁敢说“除了种族问题以外，别的公报私仇就一定可以在陪审团制度框架以内解决（而不是要求重审）”呢？哪儿有那么凑巧的事？<br />
　　而且，这个规定本身也有问题。洛杉矶黑人暴乱，是因为白人警察打黑人而被判无罪所引发的。结果就给重审了，换了一班陪审员定了警察的罪。虽然挨打的那个黑人根本就是拒捕，还有录像为证（在新闻播出时拒捕情节被剪掉了，所以才引发的暴乱），但是照判不误。据说，当时为了判他们的罪，在筛选陪审员的时候，还放弃了诸如“你敢不敢做出违背社会舆论的判决”这样的提问。反正要判你，你就跑不了：等于是把陪审团的“暴政”，换成了“群众暴政”。<br />
　　陪审团制度的另一个结果，是把辛普森给判了个终身监禁，罪名是“持枪威胁”。人们评价说“终身监禁对于持枪威胁来讲，是太重了；但对于谋杀而言，就是正好”。<br />
　　这里面大概有这么个原因：陪审员在审讯期间，是被“绿坝”的，只允许他们看到法庭允许他们看到的东西，以防他们受舆论的影响；加之“辛普森杀妻案”的法庭交锋确实对控方不利，结果就判无罪了。但是，你想找一班始终被“绿坝”完全不知道辛普森历史的人来当陪审员去审“辛普森持枪威胁”案，那总是不可能的。所以就是“终身监禁”。（上述内容参见《美国宪政历程：影响美国的25个司法大案》任东来等编）<br />
<br />
　　综上所述：司法制度总是要在“法律”和“民意”之间找平衡的，虽然看起来不该如此，但至少“陪审团制度”本身就有“诉诸人民”的含义。制度都是不完美而需要修正的，陪审团制度也是如此，但世界上本来也没什么完美的东西，所以这不奇怪。在美国这样一个最伟大的现代民主国家，有那么多黑人因为司法制度问题而冤沉大海...这个也不奇怪。在古希腊，民主派不小心弄死了一个培养出一大帮王八蛋学生的老头子，这个...这个难道奇怪吗？<br />
<br />
　　好吧，我想我已经说明了为什么“苏格拉底之死”本身不是什么应该有重大意义的事件，虽然它被恶意赋予虚假意义以后确实有重大危害。那么，这跟“犹太-基督教的毒”有什么关系呢？下面解释这个问题：<br />
<br />
　　大体上讲，希腊-罗马异教时期创立的民主国家，人们（民主派）普遍认为权力是属于人民的，不相信有凌驾于人民或人类的存在**；他们当然也敬神，但是，因为多神教的宽松性，这种敬神没有重大的实质意义。<br />
　　一神教的缺德之处就在于它把人类的宗教本能给极端化了：既然你要敬神，干嘛不敬得彻底一些呢？这种思路既简明又混账，比罗永浩喜欢曾哥就把曾哥的图集贴在牛博网首页上恶心大家还混账千百倍；罗永浩要是根据这种混账逻辑行事，就不只是贴图的问题了，恐怕得不舍昼夜地追随曾哥的行踪当疯狂粉丝去了。<br />
　　一神教的这种疯狂性带来了对“绝对真理”这个概念的痴迷，虽然这个概念的痴迷者我们司空见惯，但实际上它却和一神教紧密相关，不是自古如此的。罗马世界和中国世界的多神教都愿意随时吸收新的包括异教的神祗（只要它有影响力），象儒家这样一本正经的思想也是强调“中庸之道”的。<br />
　　“绝对真理”和“人民”当然是有冲突的。犹太-基督教徒如果不反对民主的话，又怎么解决这种冲突呢？办法无外乎这么几个：<br />
　　一、糊弄过去，避而不谈，假装没有。<br />
　　二、认为“法律（作为绝对真理的现实化身）高于一切”，“法律”而非“选举”才是保障人权的底线。问题是：从事实来看，他们根本贯彻不了。<br />
　　三、认为“法律”和“民意”本质上是统一的，然后不着边际地给你胡乱解释一顿。这类弱智解释的路数很常见，不熟悉这种路数的人可以去听听基督教的传教或布道。盎格鲁-撒克逊政治家尤其精于此道。<br />
　　四、拼了命的诋毁异教时代的文明和民主，以攻为守。是的，异教时代确实不如现代发达，民主制度也不如现代完善，但是诋毁也要恰如其分吧？<br />
<br />
　　恩，说了基督教这么多坏话，有什么意义呢？那要看什么意义了。从颠覆民主制度的角度讲，当然是没意义的，因为民主制度也不是因为“好”才存在的，而是因为其它制度“更坏”才存在的，单说民主制度的“不好”，说再多也是没用的。<br />
　　但指出犹太-基督教的虚伪，本身就是有意义的。至于摒弃基督教的毒素以后又该怎么样，我们也许可以套用一下卡尔施密特的那段论述：<br />
　　“在坚持法律优先性的同时，施密特也强调，离开了强大的国家，再好的法律也没有意义，法律的优先性更是无从谈起；因为国家是一种法律结构，其使命和意义便在于确保法律的公正实施。因此，在法哲学层面上，法律的优先性概念与强大的国家概念之间，并不象自由主义所理解的那样，是一对水火不容的矛盾关系，相反，它们之间不但能够包容，而且必须包容；换言之，任何一个强大的国家都必须是法治国家，反过来，一切法治国家也都必须从整体上保持政治上的强势。”<br />
　　也就是说：国家必须保证人民的政治意志得以贯彻，当然是以法律的形式来保证这一点。法律本身不是绝对真理，法律是人民政治意志的体现***。<br />
　　苏格拉底死于僭主统治倒台以后的“不报复”，无数黑人冤死于南北战争以后的“给黑人选举权（加剧白人对黑人的仇视）＋不报复（因此就不能吓阻不法白人）****”，这都是国家在政治上不够强势的体现，因为国家无法以法律的形式将其政策执行到底。这种执行是否可能姑且不论，不执行而导致“法制败坏”的结果却是一定的。接下来美国为了补偿黑人，又执行了平权法案之类的涉嫌违宪的政策，等于是裁判上半场吹错了哨，下半场再找机会错回去，结果也许还算公正，但法律的尊严......。<br />
<br />
<br />
*：独裁者上台以前，会利用一切反民主言论为自己的上台制造舆论。但是，反民主言论却未必支持独裁，所以最后还是会被独裁者镇压。象苏格拉底这种支持“专家治国”的言论，也不等于支持独裁统治，反而既对独裁者提出过高的要求（要求他们是专家），又把他们贬低到一般专家的地位（仅仅是专业不同而已）。当然不可能讨好。<br />
**：对此我们参看希腊-罗马神话中那些同命运和诸神抗争的英雄，这些人即便不完全是正面人物，也绝对不是反面人物。反之，在《圣经》中，对上帝的任何不敬、不信或信仰动摇都是罪大恶极的，有兴趣的人可以去看看其中的《约伯记》。<br />
***：所谓“国家在政治上的强势”，对于民主国家而言，就只能是体现人民的意志。国家不是活人，国家意志总是人类意志的体现；而只有当国家（也就是人民）可以体现其政治意志的时候，才谈得上政治强势。此外，人民的政治意志在法律上的体现，虽然不能是“即时”的（那等于用人民的政治意志直接代替法律，也就无所谓法律了）；但也不能是永久的，不能以人民十几年，几十年甚至几百年前的政治意志来代替现代的政治意志。<br />
****：不报复也许是正当的，尤其考虑到南北战争的背景。但是，如果“不报复”成为一种明确的政策，那它的含义就一定会演化成“任何看起来象报复的事都不能干”，从而束缚住国家的手脚，使之无法保持政治上的强势。<br />
<br />
