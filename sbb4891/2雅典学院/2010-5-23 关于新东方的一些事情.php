													<h2>关于新东方的一些事情</h2>
						
						
																											<div id="postmessage_634617" class="t_msgfont"><strong>权衡再三还是在小组里说下吧。请大家不要转载。</strong><br />
<br />
来上海新东方学校有一段时间了，教SAT和托福和GRE。国外部自己的课上过，也经常去VIP那边上课。有一些事情要和组员说一下，如果以后有到新东方上托福或者SAT的朋友，可以将我说的作为参考。最近上SAT的课比较多，就以SAT为例吧。另：要上GRE的组员，可以忽略这篇文章。<br />
<br />
1、之前，上海新东方的国外考试部和VIP的北美项目其实是两个不同的机构。以前，因为VIP部门缺老师，在排VIP课程的时候经常会让国外部的老师去上VIP的课，现在也是。前不久，这两个部门合并了，但其实也就是由这个新部门统一负责新老师的招聘、培训、教研等工作，实际上还是可以把它们看成两个部门。因为，国外部招聘来的新老师是排不到大课的（大课指25人以上的班级，由国外部排出，班号没有V这个字母，比如SAT的大班叫做S0XXX（XXX指数字），而SAT的VIP班是由VIP部门排出来的，班号一般叫做VCWXXX（XXX指数字）。任何班级，只要班号出现了V这个字母就说明是VIP的课程。VIP班级指一对一、5人、10人、20人小班这样的班级。新教师只能上这样的VIP课程，拿的课酬相对少一些。要等一段时间，学生给你打分打得不错，才能上国外部排出的大班的课程。<br />
<br />
2、所以，看起来显而易见了。虽然现在上海新东方在大力推广VIP课程，但是该上什么样的班似乎就不需要多说了。不过还有更多的理由。<br />
总之，注意一点原则：永远不要上VIP的班级。<br />
所谓的VIP，管理混乱，师资力量不能说差（毕竟你要进来那肯定要有水平）但是比不上国外部的大班，价格奇贵，似乎就是一个为了收钱而存在的部门。简单说几点：<br />
它为了收更多的钱，除了基础班，还硬生生的把SAT强化班拆分成了强化班和强化后续班。一个10人SAT强化走读班收费15000+，一个10人强化后续班收费10000+，之后还有个什么狗屁模考点评班，纯粹骗人的玩意，给你几张真题让你做，然后老师来讲。VIP为了收更多的钱而搞这么多班，但是忽略了最关键的一点：质量。<br />
首先，并不是说VIP的老师就是最好的老师。这个前面已经说过了。最好的老师都去教大班去了。另外，就大班来说，4700-7800的价格，可以有44课时，7800是25人SAT的价格。而VIP呢？强化班，34节课，15000+，课时少了10节，人少了15个人，价钱翻倍。然后什么所谓的配教学助理全程辅助，送两次模考什么的，我可以说，这些都是笑话。<br />
班级并不是人越少就效果越好的。比如10人VIP，和25人普通的班级，我觉得是没啥区别的。依然是老师在讲台上讲课，学生在下面听讲或者睡觉，如果觉得人多了离老师距离太远，你还可以录音嘛。<br />
<br />
其实让我发这个帖子最大的原因，是几天前收到一个VIP那边的通知，说，上强化班严禁讲真题，只准讲官方指南（以下简称OG）。因为还有个强化后续班呢，真题要给他们讲。乖乖，多少学生不是来上个强化班就去考试的？就现在的SAT来说，OG已经有了官方的详细讲解，另外，SAT现在不是由ETS出题而是由CB自己出题，虽然考试的题量和形式都一样，但是考试的题目本身发生了一些变化。OG上的那些东西放到现在已经不合适了。我可以说，讲OG或者巴郎是不能给学生得到很大的提高的。SAT就应该以真题为主，并且着重讲考试变化的趋势。如果让我在强化班上讲OG，那是不负责任的表现。但是VIP不准。他们想让学生报强化后续，这样又是一笔收入。按照他们的说法，强化后续班才是讲真题的。强化后续班的课比强化班更少，你让我去讲真题？13套真题啊。<br />
<br />
总之，要来上SAT就别来上VIP，一无是处的部门。很多人看着VIP这三个字来报名，其实得到的却不是他们想得到的东西。其实最好的办法就是报25人班，人数不多，价格便宜一半，课还多了10节，老师也更好。VIP就他妈是个笑话.....<br />
<br />
另：请组员内部传阅，不要转载到任何其他论坛。毕竟，我现在还要靠新东方吃饭，请大家理解。谢谢。</div>

							
							
							
							
