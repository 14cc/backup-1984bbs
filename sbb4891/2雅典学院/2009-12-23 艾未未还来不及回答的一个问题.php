换句话说，一个国家的法律没有超越这个国家普遍认可的道德观，就不存在自由的讨论与抗争。<br />
<br />
关于这个，再引申一下，就是裸奔主义者，有法律规定，裸奔有伤风化，要禁止，倘若这个法律的制定是在充分民意下制定的，也排除多数人暴政的情形，那么这就是合理的，应该的，是文明使然。但如果哪天裸奔的人多了，要求修改法律，以为裸奔是更先进的文明，那么法律也会随之修改，因此，在正常的民主社会，限制你几夫几妻的是道德，而不是国家管理者，也谈不上自由的，只有统治阶级利用枪违背了文明的尺度，这才存在政治意义的自由。你且不能说，这个社会的文明观、价值观阻碍了你的自由，哪怕的消极自由，这个提法本身就是错误的。<br />
<br />
人类社会从杂交到固定配偶是进步，从固定配偶到一对一的固定配偶又是进步，从文明的角度来说，就是文明了。但在文明社会里面，还有很多异类，相当于肿瘤之类的，也不足为奇。<br />
<br />
有人就说了，旧道德旧思想，社会的桎梏，我不要，还我自由。其实，即使你不要旧道德旧思想，新道德新思想一样的要桎梏你。人类之所以为人类，为群居动物，就要有弗洛伊德的“现实原则”。不能用小孩的“快乐原则”来主导你的成人生活。<br />
