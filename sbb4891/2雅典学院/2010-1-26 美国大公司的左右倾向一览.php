																											<div id="postmessage_452603" class="t_msgfont">知识分子为何拒斥资本主义<br />
<br />
罗伯特,诺齐克<br />
<br />
秋风译 <br />
<br />
知识分子如此地反感资本主义实在是怪事一桩。与之社会经济地位相当的其它社会群体却并没有显示出同样强烈的反感。因此，从统计学上讲，知识分子真的是太不同寻常了。 <br />
<br />
　　并不是所有知识分子站在“左翼”一边。跟别的社会集团一样，知识分子的观点也是各种各样的，但这个群体对于资本主义的看法却是明显地倒向政治上的左翼。 <br />
<br />
　　此处所谓的知识分子，并不是指所有受过某种程度教育或具有某种智力水准的人，而是指一批人文知识分子，他们的职业是处理用词语表达的观念、塑造词语流给其他人接受，这包括诗人、小说家、文学批评者，报刊记者编辑及很多教授。这里不包括那些主要是制造和传播数字或用数学公式表示的信息（从事数字工作的人士）的人士，也不包括从事视觉媒体、绘画、雕塑、摄影的人士。在后面这些行业中，对资本主义的拒斥并不如从事文字工作的人士那么强烈。这类人文知识分子主要集中在某些行业：人文学术界、媒体及政府官僚机构。 <br />
<br />
　　人文知识分子在资本主义社会中生活得很好，在这个社会，他们拥有生产、接触和传播新观念的充分自由，也能够自由地阅读和讨论这些思想观念。他们的职业才能颇有销路，他们的收入高于社会平均水平。那么他们为什么如此强烈地拒斥资本主义？实际上，有数据显示，知识分子越富有、越成功，反倒越坚定地反对资本主义。他们对于资本主义的看法经常地是从左翼立场得出的，但也并不总是如此，叶芝、艾略特、庞德就是从右翼立场出发反对资本主义的。 <br />
<br />
　　人文知识分子对资本主义的拒斥，具有相当大的社会影响力。他们塑造着我们的观念和我们对社会的认识，他们在某种程度上左右着官僚机构内部的政策选择。通过论文、小册子、口号等形式，他们提供了我们表达自己思想感情的词语工具。特别是在社会越来越依赖于直接浅白的表述和信息传播的时代，他们的反对立场尤其显示出重要性。 <br />
<br />
　　对于何以有如此高比例的知识分子对资本主义持反对立场，我们应该区别两种不同类型的解释。在第一种解释中，某个单一因素导致知识分子反对资本主义。在第二个解释中，我们确定，有某个因素作用于所有知识分子，它有可能促使知识分子得出反对资本主义的观点，但这种力量是否导致某些知识分子转到抵拒资本主义的立场，却还需取决于其他一些因素的影响。总体上，它有可能使每个知识分子拒斥资本主义，但有某一个因素导致很高比例的知识分子反对资本主义。我所作的就是第二种解释。我将找出这一使某些知识分子倾向于反对资本主义，但并不是对任何知识分子都同样有效的因素。 <br />
<br />
知识分子的价值 <br />
<br />
　　如今的知识分子无不期望成为社会上最受尊敬的人，最有声望和权势，获得最高的收入。知识分子觉得他们配得上如此荣宠。然而大体说来，资本主义社会并不能如此礼遇知识分子。米塞斯曾经解释过相对于工人阶层，知识分子有一种特别的怨恨情绪，他们的社会交际对象常常是最成功的资本家，与这个集团比较，他们常常为自己低下的地位而觉得屈辱。不过那些并无这类社会交际的知识分子，也同样具有怨恨情绪，所以仅从社会交际角度解释是不够的，体育和舞蹈教练也想变成富翁，但他们未能如愿时，却并没有强烈地反对资本主义。 <br />
<br />
　　那么为什么当代的知识分子觉得社会应该给予他们最高的待遇，而不能如愿时就心怀怨恨？知识分子觉得他们是最有价值的人，也具有最高贵的美德，社会理应根据他们的价值和美德给予相应的待遇。但是资本主义社会不是实行“按照美德或价值分配”的原则。在一个自由社会中，除了个人才能，祖上的传承、运气都能使一个人成功，市场只会青睐那些能捕捉到并满足他人需求的人，至于获利有多少，则取决于需求有多大，竞争的供应者有多少。所以失败了的商人和工人并不会像人文知识分子那样怨恨资本主义。唯有优越感不被社会接受，特殊的权利不被社会承认，才会在知识分子心中产生愤恨。 <br />
<br />
　　为什么人文知识分子认为他们是最有价值的，为什么他们认为应该根据价值进行分配？请注意，他们并不是非要后一种分配原，他们也曾提出过其他的分配原则，比如平均分配，按照美德进行分配，或按需分配。分配的公平。一个社会尽管很关注公平正义，并不表示它实行了某种分配模式就可以得到这种公平正义。分配的正义应该是存在于对公正地获得的财产和服务，完全公平地自愿交换的过程中，交换的结果不管是什么样的，都是公平正义的，而某一特定的分配模式并不一定会产生这样的结果。那么，为什么人文知识分子认为自认为最有价值，并接受按价值进行分配的原则？ <br />
<br />
　　知识分子告诉我们，自有文字记录的历史以来，他们的活动是最有价值的。柏拉图就说理性思考能力比勇敢和欲望更高级，并认定了哲学家应该成为统治者。亚里士多德则主张，知识分子的沉思是最高级的活动。这也就难怪，现在保存下来的文字对这些知识分子的活动都给予了很高的评价。毕竟，进行这类智力活动、运用理智并将其记录下来的，不正是知识分子嘛。他们无异于是王婆卖瓜。那些可能把别的活动，比如打猎、比力气、或沉溺于肉欲享乐，看得比知识思考活动更有价值的人，却根本没有心思留下文字记录。只有知识分子能搞出某种理论，论证谁是最棒的。 <br />
<br />
知识分子的学校教育 <br />
<br />
　　何种因素使一部分知识分子产生了高人一等的想法？我将从学校教育这种制度谈起。由于书本知识越来越重要，学校教育这种教育方式普及开来，大群年轻人聚集在教室里念书，学习书本知识。在型塑年轻人的观念过程中，学校教育的影响大概仅次于家庭，而后来成了知识分子的家伙也都进过学校，并且都是那里的佼佼者。跟别人一比，他们觉得自己是优胜者。他们总是受到赞扬，获得奖赏，他们是老师的宠儿。他们怎能不把自己视为高人一等？他们每天都体验观念上的日新月异，他们则可以轻而易举地对付。学校教育告诉他们，并让他们得出结论，他们是优秀的人。 <br />
<br />
　　学校教育也展示从而也教给他们按（智力上的）美德获得奖赏的原则。从智力上的胜利所获得的报酬是赞扬、老师的笑脸和高分数。在学校的社会分层中，最聪明的学生构成校园中的上层阶级。尽管从来没人给他们上过这一课，但从学校教育中知识分子得出结论，与社会其他阶层相比，他们更有价值，也坚信靠这种更高价值，他们理应获得更多报酬。 <br />
<br />
　　然而，外界的市场社会，给他们的却是别一样的体验。最大的奖赏并不是给那些最能言善辩的家伙。在这里，知识分子所掌握的技能，便并不是最有价值的了。在学校中，他们总觉得自己最有价值，最配得上奖赏，最有理由获得奖赏，而资本主义社会剥夺了他们自己觉得理应获得的奖赏，那么他们怎能不怨气冲天呢？这也就难怪学，校教育培养出来的知识分子对资本主义社会抱着一种深深的敌意，当然，具体表现出来却会有种种冠冕堂皇的其它理由，直接以上述理由总是有点不合适吧。 <br />
<br />
　　说知识分子自以为应得到社会给予的最高奖赏（财富、地位等），我的意思当然并不是说，知识分子认为这种种奖赏就是他们最看重的东西。他们也许更看重智力活动本身固有的价值或时间的考验。尽管如此，他们仍然觉得自己应从社会得到最高的奖赏，他们完全能配得上最多和最好的奖赏，即使他们自己根本就瞧不起这些东西。我并不想特别强调说，这些奖赏非得进知识分子自己的腰包，甚至不一定非得由他本人获得。只要具有知识分子身份，他们就为知识活动并没有得到最高尊重和奖赏而怨恨。 <br />
<br />
　　知识分子期望整个社会就始终像学校一样，期望着在这个环境中他们照样最出色，也照样得到赏识。学校里的奖赏标准与社会上的标准如此不同，则从学校出来的拔尖者未来进入社会后通常都要经历一种心理挫折感。那些在校园等级制度中处于顶端的学生，会觉得他们不仅在校园这样的小社会中，也在更大范围社会中有资格也处于顶端，然而进入了社会，他们如果得不到如他们所期待的地位，他们就心生怨恨。因此，是学校教育制度在知识分子中间制造出了反资本主义的情绪，当然更多的是在人文知识分子中间制造出了反资本主义情绪。为什么从事跟数码打交道的知识分子没有产生同样的情绪呢？我推想是这样的：这些在数字方面有天赋的的孩子，虽也能在他感兴趣的科目中考得高分，也能得到老师的赏识，但与在人文学科方面有天赋的孩子相比，却较少获得老师面对面的关注和称赞。能说会道的技巧，使这些具有人文天赋的孩子能得到老师本人的关爱，而正是这种格外的关爱，使他们觉得，他们是理所当然应始终受到关注。 <br />
<br />
教室中的中央计划制度 <br />
<br />
　　还要进一步补充说明一点。（未来）从事文字工作的知识分子作为正式的、官方的校园社会中的成功者，奖赏则是由作为中心权威的老师分配的。而在教室、在走廊、在学校操场上还有另一个非正式的社会群体，在这些场合，奖赏则不是由某个指导中心分配的，而是由同学们一时兴致和好恶进行分配，而恰在此处，知识分子表现得却并不怎么样。 <br />
<br />
　　因此，毫不奇怪，那种由一个中央控制的分配机制分配物品和酬劳的制度，会令知识分子砰然心动，相反，对市场的“无政府和混乱”却是避之惟恐不及。实行中央计划体制的社会主义社会之与资本主义社会的对立，恰相当于由教师主导的分配与操场上和走廊内的分配之对立。 <br />
<br />
　　我的解释并不是说，学校中学业优秀者的大多数都会变成（未来的）反资本主义的知识分子。校园中绝大部分出类拔萃之辈都是精通书本知识，善于交际，强烈地追求快乐、友情、制胜之道，并能按规则游戏（或者看起来是遵守规则），这样的学生，必然会得到老师的格外关注和奖赏，进入社会后，他们通常也会干得非常出色。（在校园的非正式社会中也表现很棒，所以他们并不会全然接受学校正规制度的规范）这样的学生并不会滋生出反资本主义情绪。我们的解释所涉及的的是那部分在校园（官方体制）中居于上层，而在进入社会中却将经历相当挫折的群体，或者更进一步明确地说，是指那预料到自己可能会走下坡路的群体。在进入社会，经历社会地位之下降以前，有些聪明的学生就意识到，在进入社会后，他的地位将不如他现在在校园中，那他就将滋生出对资本主义的敌意，如果学生阅读到反映此一情绪的作品或碰上具有这种情绪的知识分子教学，学校教育不经意间播下的种子，即知识分子的这种反资本主义的情绪，则必然会进一步强化。 <br />
<br />
　　毫无疑问，某些人文知识分子在学校时就脾气很坏，喜欢提问，并不为他们的老师所喜欢。那么他们是否会想，表现最好的应该得到最高奖赏，而他们就是最好的，却仅仅由于老师不喜欢他们却得不到这种奖赏，并由此而对学校制度产生愤恨情绪？显然，我们需要更多材料来验证我们的假说。 <br />
<br />
　　但是一般而言，无可争辩的是，人们离开校园后所秉持的规范性信仰必然要受学校规范的影响。毕竟除了家庭之外，学校是孩子们学习行为方式的主要场所，因此学校教育也就是他们为进入家庭之外社会的最重要的准备。因此，一点也不奇怪，那些在学校的规范体系中如鱼得水的人会对社会不满，而没有在学校中出人头地的人则坚持另一套规范体系。如果继续由这些人塑造社会的自我形象和自我评价，则我们所看到的在语言文学方面社会总是自己反对自己，就并不令人奇怪。让你设计社会，你可能并不会刻意去设计他，而人文知识分子则会运用他们的一切势力，把他们对社会各种规范的敌意灌输在教育体系中。 <br />
<br />
　　我们对知识分子非同寻常的反资本主义心态的解释，是立基于有点似是而非的社会学概括上的。 <br />
<br />
　　在某一社会中，年轻人在走出家庭所进入的人生第一个团体或制度中，如果表现很出色，就会把这一制度的规范内化为自己的行为规范，并且期望外面的世界也是按这些规范运转的。他们会觉得自己有资格获得按这些规范所应得的好处，或者至少达到在这些规范下所能达到的地位。而在他们人生第一个团体或制度的等级体系中处于上层地位的人，如果在进入外面世界后经历了（或预料到会经历）社会地位的下降，则会在失落感的驱使下，倾向于反对这一社会制度，对其规范心怀敌意。 <br />
<br />
　　请注意，这一点并不是确定性的定律。并不是所有经历过社会地位下滑过程的人都会对社会制度产生敌意。这种地位下降只是促使人们敌视社会的一个因素，此一因素作用的大小在不同人那儿也是大不相同的。对上层阶层的地位下降，我们可以区分出不同的类型：一种是他比别的社会集团得到的少（此处并没有某一集团地位上升）或者是没有增加，跟理应在自己之下的集团相比，得到的一点也不多。这是第一类地位下降情况，这会使他愤怒，觉得受了侮辱。第二类则比较更能容忍，很多知识分子按还是颇（据他们说）关注平等，只有很少一部分鼓吹知识分子实行精英统治（，所以他们不大会为了社会没有让他们进行统治产生挫折感，而生气）。我们的结论是第一类地位下降，特别容易招致怨恨和敌意。 <br />
<br />
　　学校教育体系很少传授和奖励那些在进入社会后能获得成功（毕竟学校只是一种专门化的制度）的技能，因而它的奖励制度与一般社会截然不同。这必然导致一些人在进入社会后，要经历社会地位下降及其所带来的痛苦和愤怒。早些时候我说过，知识分子期望社会只是学校的同质放大。现在我们看到了，由失落感而生出的怨恨敌意，乃是因为一个简单的事实：学校（作为进入社会的第一个非家庭的专门化的社会组织）并不是社会的缩影。 <br />
<br />
　　现在我们似乎可以解释，受过学校教育的知识分子，为什么会有那么高比例的人反对他们的社会，而不问其社会性质，不管它是资本主义社会还是共产主义社会。（跟资本主义社会中，与知识分子处于同等社会经济地位的阶层相比，知识分子中反对资本主义的比例高得异乎寻常。另一个问题则是，与别的社会中，反对其所在社会的知识分子的比例相比，是不是也高得异乎寻常。）共产主义社会中的知识分子对其制度的态度恐怕大致相当；那儿的知识分子恐怕也对那一制度表示敌意。 <br />
<br />
　　所以，我们的假设需要再细化一下，以使它不会对随便一个什么社会都可以套用。每一社会的学校教育体系，不可避免地都会在其不能得到社会最高奖赏的知识分子中间，制造出反社会的心态。资本主义社会的特别之处在于，它宣称它的奖赏只针对个人才能、个人创造性和个人特长。在种姓制度或封建社会成长起来的人，则决不希望、或认为根本就不应该按个人价值分配财富地位。但不管你怎么想，资本主义社会给予个人的回报，所依据的唯一的标准，是其满足市场所揭示的他人的欲望的程度，它只问你的经济上的贡献，而不管你的个人价值。不过它非常接近于按价值获得奖赏――价值与贡献基本上可以通用――因而资本主义社会培育出的期望跟在学校中得到的观念差异不大。一般社会的的风气跟学校里的风气非常接近，而正是此种相似，导致了怨恨。而资本主义社会只奖赏个人成就，或宣称如此，因而是冷落了知识分子，他们觉得他们才是最有成就的，因而也就特别痛苦。 <br />
<br />
　　我想还有一个因素起了作用。学校教育培养出如此强烈、广泛的反资本主义心态，另一个重要因素是人的多样性。很多未来在经济上会大获成功的的人，都上了别的学校，知识分子就没有养成一种心态，就是其实有很多人比他们更优秀。当然，尽管很多上层阶级的孩子进了别的学校，不过在一个开放的社会，也有一些学校包容了各种各样的学生，其中有些未来会挣大钱，比如企业家，而未来成为知识分子的人，则满怀怨恨地回忆起，当年自己在学术上是如何地出类拔萃，而现在有钱有势的家伙，当初有什么了不起。社会的开放导致了另一结果：学生们，不管未来是做了人文知识分子，还是别的职业，都不清楚他们未来的人生路是什么样的。他们充满希望。而一个开放社会则令那些早年的期望破灭了。在一个开放的资本主义社会，社会似乎宣称，那些最有才能和最有价值的人有望爬到社会最高层，在学校，他们依靠学术上的出众之处而获得最高地位，于是他们得到的看法就是，他们自己正是最有价值的，最有资格得到最高的奖赏，然而，到了最后，这些最有信心、最满怀希望的学生却看到，那些在学校中他们根本不放在眼里的家伙，却爬得比他们还高，抢走了他们觉得本应属于自己的奖赏。由此而对社会心怀怨怼，有应何奇怪之处呢？ <br />
<br />
假说的进一步细化 <br />
<br />
　　我们已经让我们的假说更精确了。导致（人文）知识分子反资本主义的，并不是随便一种什么学校教育，而是某一特定社会中的学校教育。毫无疑问，这个假设尚需进一步细化，不过也差不多能说明问题了。现在该把这一假设应用于社会科学家，离开书房中的沉思冥想，用更广泛的事实和数据来进行验证。不过，我们尚不能肯定，在哪些领域，我们的假说会得到同样的可以验证的结论。首先，我们可以料想，国家的教育体系越具有精英化倾向，那儿的知识分子就越容易倒向左倾（想想法国吧）。第二，在学校里属于“大器晚成”的学生，一般不会产生那种自以为应获得最高地位的想法，因而，大器晚成的知识分子与成名较早的知识分子相比，只有较少比例的人会产生反资本主义的心态。第三，我们假说适用于在这样的社会（不像印度那样的种姓社会）：学校中出众的学生可以指望在进入社会后更上一层楼。迄今为止，西方社会的妇女并不抱有这种期望，那么我们可以推想，女学生中，在课业上表现突出，但在进入社会后地位下降的，并不会产生如男学生那样强烈的反资本主义的心态。我们进一步可以预料，如果一个社会，男女在职业机会方面趋于平等，知识女性中表现出反资本主义心态的比例，将与男性知识分子中一样非同寻常地高。 <br />
<br />
　　有些读者可能怀疑上述对知识分子的反资本主义心态的解释。随你的便，反正我觉得我已经指出了一个重要的现象。我们上边所做的社会学的概括，确乎具有直觉的性质，不过应该八九不离十吧。校园上层阶级中，某些经历了社会地位下降的人总要作出某种重要的反应，必会产生对于一般社会的敌视。知识分子的这种反应如果不是强烈地、普遍地拒斥资本主义，还能是什么？我们从一个令人迷惑、需要解释的现象入手，我想我们已找到了明显摆在那儿的（如上所述）解释性因素，因而，我们相信，我们的假说说明了某些现实的现象。（2000，2，29译完） <br />
