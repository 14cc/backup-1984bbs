													<h2>万里和中央党校年轻教授的最新谈话</h2>
						
						
																											<div id="postmessage_341878" class="t_msgfont">2009年11月24日 星期二 <br />
&nbsp;&nbsp;<br />
<br />
&nbsp;&nbsp;<br />
by 姜平 <br />
&nbsp;&nbsp;<br />
被万里称作「忘年交」的那位中央党校年轻教授，最近急匆匆去请教万里。他说，这次问题是党校其他同志提出的，也包括他自己。<br />
<br />
下面是他们谈话的部分内容：<br />
<br />
从1989年5月开始，江成为党的总书记，至今在党内排名仅次于胡锦涛，江的「三个代表」写进党章，表明这是党的行动指南。在党的「辉煌60年」里，江泽民占了20余年。而且如果党章不修改，意味着江的所作所为就是党的指导思想。<br />
<br />
但，前不久，西班牙国家法庭宣布，在历经两年详细的搜证调查后，同意受理「起诉江泽民、罗干、薄熙来、贾庆林和吴官正等五人所犯下的群体灭绝罪及酷刑罪等重大罪行」，并且已给五名「党和国家领导人」寄发了调查委托书，并通知他们有四至六周的抗辩期。如不抗辩，过了此期限，判决生效，将面临至少20年的徒刑，以及须支付受害人索赔金。可是，全国都知道，对法轮功的镇压至今也没有停止，所以根本谈不上抗什么辩。<br />
<br />
中央党校教员们的问题是，当学员们问他们「建政60年、辉煌60年，为何党领袖、国家主席、党和国家领导人成为国际通缉的群体灭绝本国百姓的罪犯」时，他们真不知如何回答。<br />
<br />
万里说：如果实事求是回答这个问题，其结果是什么，谁心里都清楚。共产党执政合法性的最大理由之一是「几千万革命先烈换来了红色江山」，包括我在内，为什么愿意抛家舍业也要跟着共产党闹革命呢？为的是实现当时我们中国共产党设立的目标和理想。<br />
<br />
当时，我们党的社论和党的领导人的讲话，都是批评国民党「一党独裁」，向全中国人民做出承诺，要建立一个民主、自由、独立的国家。毛主席在三四十年代的许多著作中都有那些承诺。可是，到了五六十年代都被修改掉了，这说明我们党不是后来才变坏的。<br />
<br />
我印象很深的一件事就是，十六大召开之前，中央五次会议五次讨论通过，让江泽民把党政军职务都交给胡锦涛，结果十六大一中全会，我和几位老同志请假没参加，事后才知道江泽民少交出一个职务，竟然没事。这说明我们党没有法。<br />
<br />
一个没有注册，又没有法制的政党，执政必然会出问题。现在就出问题了，西班牙国家法庭宣布五个「党和国家领导人」有罪。这个问题不仅仅是他们自己的问题，也牵扯到我们党的未来，也牵扯到如何给我们党定性的问题。 <br />
<br />
最后，年轻教授临走时，突然想起什么，转过身来问：您会不会退出共产党？<br />
<br />
四目相视，万里把目光轻轻移开，半晌，道：当时，我们党向全中国人民做了承诺，要建立一个民主、自由、独立的国家。这些承诺的确吸引了我，我才发誓要为之奋斗一生。△</div>

							
							
							
							
