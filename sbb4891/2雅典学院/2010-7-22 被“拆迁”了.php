													<h2>如何做一个“合格”的钉子户</h2>
						
						
																											<div id="postmessage_755339" class="t_msgfont">如何做一个“合格”的钉子户<br />
<a href="http://news.163.com/07/0323/02/3A83SG7Q0001124J.html" target="_blank">http://news.163.com/07/0323/02/3A83SG7Q0001124J.html</a><br />
<br />
<br />
前言房屋拆迁中当事人之间的矛盾是无法避免的，特别是商业开发拆迁中，被拆迁人对立情绪特别大，如何消除当事人双方矛盾，将矛盾减到最小。本文力求从拆迁钉子户角度来分析矛盾产生的根源、对策。着重从拆迁钉子户分类、产生的原因、目的、条件、手段、措施、步骤等方面来说明现行的房屋拆迁工作中急待完善的地方，力争将房屋拆迁工作做细、做实、做好。为城市建设发展作出贡献。<br />
<br />
古人云:“知己知彼,百战不殆”动迁工作无疑是和平年代的“战争”。战役的双方是不对等的，一方是散兵游勇，一方是机械化军队。一般情况，动迁人员熟知法律法规，熟知每一户情况，而对于每一户该具体如何说，如何做，每一句话都会进行认真安排，如同战争前的部署。常规做法先易后难，选择突破口，突破口的选择原则是该户人家位置关键，最好能够起到割断分裂房屋的作用，其次该户在当地有些影响，面积亦不宜过大，便于操作，能起到“以点带面”的作用。被拆迁户要想在这场“战争”中立于不败，就要知道什么是拆迁？对拆迁的程序、动迁人员的手段及战术要有基本的了解。抓住每一环节，找其漏洞。<br />
<br />
首先我们来分析拆迁过程中被拆迁户的心理状态，说实在话，当拆迁公告出来时,相信每个人心里都发毛,不知道怎么办好，比较典型的有下列四种心理状态：<br />
<br />
一、如意心理这是被拆迁户获悉自己所有或使用的房屋要进行拆迁时表现出一种愉快和兴奋的行为特征。形成这种心理的原因是被拆迁户对旧居的厌烦和对新居的向往。当拆迁实施单位进入现场时，被拆迁户一般都是态度和蔼、热情主动、积极配合。拆迁工作人员与其相处也比较和谐。<br />
<br />
二、恐惧心理当房屋拆迁工作进入实质性阶段时，部分被拆迁户怀有一种恐惧心理状态。他们在不同程度上关心自己利益的实现，主要特征是表现出忧虑和慌张，在这种情况下，被拆迁户对拆迁工作人员一般缺乏信任感，生怕工作人员克扣面积、贬低价格及折旧率等。凡事总是斤斤计较、举棋不定。<br />
<br />
三、消极心理有些被拆迁户当遇上房屋拆迁的规定、标准、和原则与自己所要求的条件不一致时，就处于一种消极心理状态，通常表现出一种不卑不亢、行动迟缓的行为特征。<br />
<br />
四、横缠心理当房屋拆迁工作进入实施阶段时，部分被拆迁户认为这是实现个人利益的关键时刻，力求经济利益或其他利益最大化，以拒不拆迁为筹码，表现为横扯和要挟行为。上述消极心理和横缠心理就是通常所说的“钉子户”，前者表现软拖，后者表现硬抗。但二者对房屋拆迁工作的影响基本一致，也是本文论述重点。下面从7个方面分别说明如何做一个“合格”的钉子户。<br />
<br />
一 钉子户的分类：<br />
<br />
1、漫天要价型：漫不讲理，把动拆迁视为捞一票的机会，撒无赖。翻手为云，覆手为雨，自以为老子天下第一，这一类人，一般没有后台，没有关系，但有三四个狐群狗党，给他当狗头军师，自己并没有多少文化，也不懂多少政策、法规，这类人中，地痞、流氓，无业人员、刑满分子居多。一般武来！<br />
<br />
2、后台撑腰型：有一定的社会关系，自持无恐，有文化、懂政策，喜欢钻政策的空子，其背后往往有高人的指点。他们以利润为目标。当他们的目标没有达到的时候，高层的电话、条子纷纷涌来。他们会见好就收，只要达到目的。一般文来！<br />
<br />
3、自我保护型：他们是社会的弱势群体，收入较低。以下岗工人、回城知青、帮困人员为主。他们的生活磨练太多了，以至不信任任何人，他们的信念是坚持到底就是胜利，他们对今后的生活顾虑太多了，对生活没有多少信心，他们计算补偿能不能买套房以后，还节余一点生活费。他们会耍无赖，会点小聪明，他们惧怕权威，对政府不信任，喜欢听小道，他们信奉只有自己才能保护自己。动迁中，最会漠视他们的利益，因为动迁人员喜欢豆腐挑嫩的吃，柿子捡软的捏。<br />
<br />
二 产生钉子户的原因：<br />
<br />
钉子户除了主观原因以外，客观上，当事人双方没有完全体现平等、自愿、等价的交易原则。其次，工作方式简单、粗暴，在政策面前不能人人平等，都是钉子户产生的原因。<br />
<br />
三 钉子户的目的：<br />
<br />
要知道绝对正确的拆迁之上还有一个绝对正确的人道主义。要给对方压力以获得最大的经济利益或其他利益。不过见好就收，戏不能演过头。<br />
<br />
四 钉子户的条件：<br />
<br />
其一：吃透各种法律法规。其二、皮要厚、嘴要凶、脾气要大、会耍赖。其三、要有“良好”的心理素质，大风面前不弯腰，天塌下来撑得住，遇事不慌乱，钉子户的成功与否，在于最后一分钟。如果家中有见风即倒的老人，则是王牌，因为谁也不敢动。所以不是每个人都可以当钉子户的。<br />
<br />
五 钉子户的手段：<br />
<br />
要文斗，不要武斗；要斗法，不要斗狠；要智斗，不要力斗！<br />
<br />
老百姓和拆迁利益集团的斗争一直血光不断。跳楼、自焚、服毒、斗殴、上访、拦车、示威、静坐、绝食、阻断路桥、冲击国家机关……不一而足！自杀壮烈，但代价太大，效果不大。暴力反抗，拆迁集团人多势众，且有黑白（所谓“白道”更可恶）协助，明摆着是吃亏。尤其不可以采取阻断路桥、冲击国家机关等手段。因为这是卑鄙的利益集团最希望出现的情况。一旦如此，他们即可将简单的拆迁经济事件上纲到治安事件，甚至政治事件，顺理成章地通过他们掌握的一部分国家机关轻而易举地将被拆迁户“名正言顺”“合法”解决。<br />
<br />
大量的案件证明：1.掌握证据，抓住拆迁集团违法违纪把柄这实在简单，一抓一大把），2.吃透各种法律法规（这最要紧），一定可以打垮利益集团！调查发现：在拆迁官司中，知识分子几乎没吃亏的，而吃亏的大多是文化不高的劳苦大众。只要被拆迁户掌握以上两条，在加上紧扣《宪法》以及党和国家领导人对拆迁的言论，就胜券在握……<br />
<br />
记住：要达到目的有时不可以用直接手段，间接手法更有效，这就是有的象36计中的“围魏救赵”。<br />
<br />
六 钉子户的措施：<br />
<br />
1 拖：生病住院，离家出走，找不到人，都可以。在拆迁的每一个环节上尽可能拉长时间，给他反反复复。时间就是胜利。<br />
<br />
２撤：面对官府和暴力机关、医院１２０－殡仪馆，单个被拆迁户是弱小的无力的，注意：要文斗不要武斗。和他们去拼是一定吃亏的。拿好所有的房产文件，拒绝在任何文件上签字（当然，写上他们反对《宪法》，批判他们是法西斯之类的谴责文字还是可以的。），拒绝任何所谓”安排“。收集好一切证据，不能签任何字。尤其在我们江苏，公正的那一天为期不远了。<br />
<br />
３抓：你的拆迁对手们一定有大量的弱点，抓住它们不放。尤其是他们大量违反的“公开公平公正”的拆迁原则：比如说：给某些有背景的被拆迁户巨额补偿费用，你可以要求同等待遇呀！<br />
<br />
4 访：一去上级机关或者直接北京去上访。 二找人大代表，人大代表也是一支不可小瞧的力量，要想取得满意的结果就需要人大代表的支持，给邻居每个人分配一项任务，就是找人大代表，并且让他们在你写的上访材料上签字，这就意味着政府的有关机构必须以某种姿态来回复，这种回复有时可以作为有利证据；希望大家想办法能够说服他们。三找律师咨询，在接触过程中，律师有时会陷于矛盾之中，为了代理案件就要展示自己的风采，他如果将案件的全部解决方法都说出来，可能你们就不请他了，如果不说呢你们可能不信任他，所以最好想出一个办法来解决这些矛盾，具体的方法我也在探索之中。<br />
<br />
5 谈：告诉他们，你们支持拆迁，但反对违法拆迁，一定要把违法问题理顺才可以进行。记住，永远不谈补偿问题，决不可以。只谈他们的违法乱纪问题，告诉他们，名不正则言不顺。一定要他们解决所有违法乱纪（不违法乱纪，怎么搞拆迁）后才谈拆迁。一定要逼他们主动和你谈条件，不满意永远和违法拆迁做斗争，绝对自己不先谈条件。以上5字诀应当贯穿整个拆迁过程，适时合理利用。<br />
<br />
七 钉子户的步骤：<br />
<br />
要做一个“合格”的钉子户，在拆迁的每一时段要要有不同的对策。<br />
<br />
第1阶段 公告阶段<br />
<br />
当拆迁公告出来时,看清楚拆迁公告内容并最好进行拍照加以证据保全。作为拆迁公告它是拆迁的法律依据,出了公告也表明了它已具备拆迁的条件并领出了拆迁许可证,拆迁公告一般内容写着建设项目名称、拆迁人、拆迁实施单位、拆迁安置补偿标准、拆迁期限、拆迁范围、立项和立项文号,规划和规划文号,土地定点和土地批准文号。你可去对照一下公告是否缺少这些要素,特别是缺少拆迁许可证五要素中的三要素:立项,规划,土地批准书。缺的话你就要小心了。拆迁公告的日子和有效期你也得看清楚,过了有效期还没拆的话,他必须去补办。此阶段对策要点是装聋作哑；背后鼓动。<br />
<br />
第2阶段 商谈、协议阶段<br />
<br />
房屋拆迁都以土地为中心而进行拆迁，所以必须对拆迁后所建设的项目要了解,是作为公用事业还是以经营性的商业开发,土地性质是划拨土地还是出让土地，以划拨方式取得土地使用权的的必须符合国土资源部颁布的第9号令《划拨用地目录》，不符合此目录取得划拨土地，将追究国土资源局领导责任。国土资源部第11号令（2002年7月1日起施行）《招标拍卖挂牌出让国有土地使用权规定》中的第四条明确规定：商业、旅游、娱乐和商品住宅等各类经营性用地，必须以招标、拍卖或者挂牌方式出让。未经招标、拍卖或者挂牌方式出让取得的土地,此项目不合法,追究国土资源局领导责任,拆迁行为不成立属违法。<br />
<br />
房屋拆迁管理部门不得作为拆迁人，不得接受拆迁委托。另外还应该了解拆迁实施单位资质情况，是否超资质拆迁，（3级资质为3万平方米以下）是否持证上岗，拆迁资金是否到位。<br />
<br />
此亦为拆迁双方互相试探、互相摸底阶段。动迁补偿弹性也很大，被拆迁户要忍耐，示弱，采用“拖”字诀，逼迫对方主动抬高补偿水准。别急~，你急他们就开心~；要沉住气~，让他们急~！！！！！不能随便签字，因为一旦你签了字，就视为认可，一定要盯住自己的心理价位，不到万不得已不降价；你没偷没抢，只是在争取自己的合理要求，一定要心平气稳……心理素质一定要好噢~，千万不能怕他们的恐吓~~。要知道拆迁就是一种心理战,就看你有多大的忍耐度, 拆迁工作人员手段不外就是以关心骗人、拿文件压人、以小利哄人、以强制吓人等等，采用轮翻轰炸，甚至有时间动用流氓、涉黑分子，很多被拆迁户经不起这样心理上折磨。投降了！能挺过这一关的人就是基本“合格”的钉子户。要坚信，大闹多给；小闹小给；不闹不给的方针。团结就是力量！这里要注意闹事情不能有头头，因为最出挑头的人往往是对方收买的对象，如果被买通了，大家就象散沙一样团结不起来了。有事情就私下集体讨论，单独去谈,谈完后再进行集体讨论，团结加互通信息很重要。有想法以挂号信的形式最好,因为你跟对方说过的话都可赖的,。寄出去的信对方不理你,你可告知他,不是你不要和他们商量,是你们不跟我谈,把皮球踢给对方。挂号寄给哪些地方呢？拆迁办,建设局,对方当事人,,信要留底,和寄出去的信一起敲上骑缝章,信封和信件也要骑缝章。此阶段对策要点是漫天要价；满足就签。<br />
<br />
第3阶段 听证调解、行政裁决阶段<br />
<br />
1、了解是否拆管理分离。现在好多拆迁管理部门和拆迁实施单位为两块牌子，一套班子。既是“运动员”，又是“裁判员”，没有做到政事、政企分开。这个是违规的！<br />
<br />
2、对方一发裁决通知就寄挂号信给拆迁办,建设局,对方当事人,并将裁决通知复印留底后一并连挂号信退给裁决机关,要求查看五项文件和拆迁许可证，并且对照一下相互间有没有矛盾。记住！这是你的权利！拿不出就有问题，不能拆！五项文件为：（一）建设项目批准文件；一般由地方计划委员会批准立项。（二）建设用地规划许可证（附红线图）；一般由地方城市规划行政主管部门核发。（三）国有土地使用权批准文件；一般由地方土地行政主管部门颁发。（（四）拆迁计划和拆迁方案；是指拆迁人对将来准备实施的拆迁活动所制定的计划和方案。（五）办理存款业务的金融机构出具的拆迁补偿安置资金证明；由拆迁人开设房屋拆迁补偿安置资金专用帐户，一般由拆迁人、金融机构、房屋拆迁管理部门三方监管。<br />
<br />
地方政府不得违反法定程序和法律规定，以政府会议纪要或文件代替法规确定的拆迁许可要件及规划变更，擅自扩大拆迁规模。详见（国办发&lt;2004&gt;46号国务院办公厅《关于控制城镇房屋拆迁规模严格拆迁管理的通知》）。<br />
<br />
不要以为拆迁管理部门颁发拆迁许可证就一定五项文件齐全。现在好多项目证件不齐全照发拆迁许可证。胆好大的！<br />
<br />
违法办理拆迁许可证,资料显示目前全国好多城市都有此现象,例如： 1 上海的周正毅(号称资产有150个亿)的房屋开发也出现了拆迁补偿安置资金不到位和土地问题. 2 嘉禾事件，嘉禾县珠泉商贸城是一个以商业营业用房为主的房地产开发项目。现已查明，嘉禾县在未进行规划项目定点的情况下，为开发商发放《建设用地规划许可证》；先办理《建设用地批准书》，再补办土地使用权挂牌出让手续；在开发商未缴纳土地出让金的情况下，发放《国有土地使用证》。在缺乏拆迁计划、拆迁方案和拆迁补偿安置资金足额到位证明等要件的情况下，为拆迁人发放《房屋拆迁许可证》；在没有按规定程序举行听证的情况下，对11户被拆迁人下达强制拆迁执行书。在项目实施过程中，县委、县政府滥用行政权力强制推进房屋拆迁，去年12月份以来，先后对11名公职人员进行了降职、调离原工作岗位到边远乡镇工作等错误处理，并错误拘捕李会明等3人。<br />
<br />
被拆迁户要认真核查5项文件，特别注意土地的取得和是否足额拆迁补偿安置资金存入专用帐户。肯定会有所收获。这样和拆迁人谈判也就游刃有余.在没有协商好前不能签任何字，保管好所有的房屋证件,因为这是你的原始股票!<br />
<br />
3 抓住要害：“公开”“公平”“公正”********一招致敌。例如，评估单位没有选择、评估结果没有公示、暗箱操作、给某些有背景的被拆迁户巨额补偿费用.............等等。数不胜数，你可以要求同等待遇呀！强烈要求上榜公布已经走人的被拆迁户补偿，按规定，这是必须的！现实中几乎每户被搬的人家都被要求保密补偿费（否则，奖金、补助没有.............等等）。为什么他们要如此。因为只有不让被拆迁户互通信息，才可让其他人心中无底，无法参照，才可以进一步压底其他被拆迁户的补偿费。既然你不敢根据国家要求“公开”“公平”“公正”，那么，咱们就好好聊聊价钱！！！！！不要钱只要房子的人家，要求同一家评估单位对自己的原有住房和给的住房一齐评估。--其中奥妙自己体会。<br />
<br />
能挺到第3阶段的被拆迁户都是好样的，也是团结的关键时刻，坚信成功在于最后一分钟，要相互支持，大家一起撑；要有强迁也一起的信念！！！！此阶段对策要点是老生常谈；对天鸣冤 ！<br />
<br />
第4阶段 执行、行政救济阶段<br />
<br />
1 现行政策环境下，行政强拆的可能性小了，司法强拆多了。现在行政救济进入谎谬的司法怪圈，例如，建设局通过单方面的裁决进行拆迁，双方当事人对裁决不服在三个月内可进行起诉，现实情况是对方当事人（拆迁人）在作出裁决后也对裁决不服进行起诉（房屋拆迁管理部门），恶人先告状，一起诉就进入了司法程序，就可强制拆迁，在法院的帮助下甩掉被拆迁户进行强拆，把被拆迁户带入被告缺位的怪圈中。简直就是猪八戒倒打一耙。强拆目的达到后对方当事人再进行撤诉，这样就免去了法院的责任。受害人不服你去告吧，拆了就不怕你告了。受害人起诉，法院审理，一审谎唐的判决下来：举证证实非法颁发拆迁许可证，但拆迁合法性与本案无关，裁决合法，安置合理，维持原裁决。这种情况发生我们怎么办？应该同时向人民法院提起诉讼，状告房屋拆迁管理部门非法颁发拆迁许可证。由于拆迁许可证案件审理结果可能影响拆迁裁决案件的最终胜负，人民法院在审理许可案件时有可能中止裁决案件的审理。事实上按现有法律法规来讲在城市房屋拆迁中根本领不出一张合法的《拆迁许可证》。其中重要原因违反了《土地管理法》，当然领不出合法的《拆迁许可证》。法律规定开发商的土地使用权取得必须通过招标、拍卖、挂牌。但土地证还在老百姓手里没有经过注销和变更土地登记,是受法律保护的,不能进行招、拍、挂，按《土地管理法》规定不能办理国有土地批准书,也不可能和开发商签定国有土地使用合同书，这就好比一女不能嫁两夫。同时《拆迁条例》规定具有拆迁人资格的拆迁人，必须持有国有土地批准文书才能办理拆迁许可证，一方面国土局不能办理国有土地批准书，另一方面必须具有国有土地批准书，所以按现有法律法规来讲是永远办不出一张合法的拆迁许可证，永远办不出,办出来的话全都是不合法的。说得简单点，假如土地证在老百姓手上，国土局发出国有土地使用证给开发商，国土局直接违反《土地管理法》和“一物一权”的民法基本原则；假如国土局没有发出国有土地批准书，建设局发出拆迁许可证，建设局就违反《拆迁条例》。没有拆迁的合法性，哪来裁决合法性，更哪来安置合理性！不过到现在还没有听到多少拆迁户赢了官司的！（难啊）<br />
<br />
2 君不见哪个城市的拆迁最后不是靠拆迁双方的谈判解决的，经过以上数个回合较量，当钉子户肯定比听话搬迁能得到更多的实惠，谁能坚持到最后，谁就获得胜利。<br />
