1.不显山不漏水地替代共产主义意识形态，这个动作，在江时代是这样尝试的，3个戴表就是想干这件事的。当时配套的口号还有以德治国。其社会实践效果我们看到了，那就是共产匪党卸掉了以前不得不背着的包袱，不用为共产马列意识形态的失败而作出交代，不必再扮演为人民服务的光辉角色，回归到了传统的统治集团轨道上来，用家国一体的传统政治伦理辅以民族主义的鼓噪作为意识形态，唯一还要继续解决的问题就是合法性的问题，这个可以由继续增长的“经济”来提供。<br />
2.用传统文化中最有利于专制统治的儒家文化来占领国民意识的领地，是一举两得的高超政治策略，一方面这可以接续尚未彻底退出的马列意识形态，只要继续保留与天下利出一孔相兼容的理性主义社会理论部份即可；另一方面可非常有利于抵制西方非本土的现代政治文明与思想，而且我们已经看到：效果奇佳，连自由主义阵营中的很多知识分子都自觉不自觉地放下武器，另辟中学为体西学为用的旧窠、蹊径。<br />
3.在一个已经实现了宪政、法治化的现代文明社会里，儒家教义无法实践其家国一体的政治伦理，故只能起到个人生活层面的礼仪教养的熏陶作用，这个虽然是儒学功效的支流末节，但亦是有益于社会稳定的本土文明和传统文化成分。到那个时候它只能是无害的，亦无须刻意批判的。<br />
4.文明文化的发展轨迹应该是历史的结果，而不应该是人为的刻意的安排，就像国家、社会不能用马克思的科学理性主义来规划一样，我们在已经与传统断裂的现实文化态势下，苦苦地去为人们安排一步到位式的接续工程，做华夏文明规划的马克思主义者，或者是拯救者什么的，是不是符合“世界是大致的，不可以由人规划的”这种自然思想？从华夏文明产生以来，人类世界上还有其他的文明，相继消亡，它们未必就比苟延至今的文明粗鄙落后。难不成在文明的时间延续上，“长久”“活着”就是硬道理么？自然规律是新陈代谢的，当今的中华文明硬要是衰亡，我们难道还想做上帝，伸出挽救之手不成？何况，文明的主要存在形式是语言、文字，至于其他的内容成份或部分，我看还是不必太自我看重了吧。文化民族主义可以休矣。<br />
