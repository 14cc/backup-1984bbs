													<h2>中国传统文化中最致命的东西――利出一孔</h2>
						
						
																											<div id="postmessage_414417" class="t_msgfont">中国传统文化中最致命的东西是管仲的“利出一孔”――利益只能出自一个孔（管道）。怎样才能做到呢？必须只让王权（后来是国家和政府）掌控全部财富的生产和流通。这思想虽然不像三纲那样为人所熟知，但却早已深入到人们的骨髓里了。于是百姓盼明君，以期得到自上而下的恩惠。所以，中国历朝历代都没生发出以私有制为基础的市场（自食其力）。中共骨子里也有这种积淀，很容易接受社会主义共产主义，因为这两样舶来品恰恰可以达到利出一孔的目的。<br />
<br />
因此，中国被共产恶魔缠身，一方面有被强加的外因，一方面也有传统文化的内因。不幸的是，这一内因至今还在很多普通百姓头脑中起作用，比如他们经常呼吁政府加强监管什么的。这一不经意的呼唤其实透露的是他们被管惯了、希望继续被管的无意识奴才心态。有了这样的传统文化，你给他自由，让他民主，他们还真的不习惯。<br />
<br />
“管”这个字（TMD还正好是管仲的管），除了管道（其口为孔）意外，当然还有管理的意思。管理自然要靠理性，马克思主义正好就是那种以为依靠人既有的知识和即时的智慧和能力，就能打翻旧世界、创建新天地的狂妄思想，于是一切就都要人为地计划，计划完了要实施，当然就离不开“管”。一个个的人不好管，就把他们弄成集体（阶级划分也算）。于是，集体主义的计划社会大规模地建开了。结果是：民不聊生。<br />
<br />
如今改革开放了，但几千年来管惯了，还有希望继续管和被管的想法，这个顽疾消除起来何其难啊！继续依靠辛亥五四共产文革的理性去改？以为那样快刀斩乱麻就可以迎来合理的制度？历史的经验已经给出答案：万万不行！所以，较好的出路就只能是渐进。而且因为民主有其致命缺陷，这条渐进之路就未必要走民主，或者起码先不要民主。<br />
<br />
从世界各国的发展路程看，比较好的制度都是从自己历史文化中自然演进而来的，比如英美。凡革过自己文化命的国家，都有过血腥的长期的折腾，比如法俄中。要避免重蹈割断历史而造成的覆辙，就必须重新回到自己的历史文化中。中国的传统文化当然就是儒家，尽管它被统治者从各种角度玩弄过，但毕竟有着深厚的民间基础。因此，回归儒家是正确选择。<br />
<br />
至于儒家能否发展出人们习惯性认为的好的主义（个人主义、自由主义）和制度（民主宪政市场等），取决于三方面。一，儒家本身的因素。二，已经被批判中断过多次的儒家能否人为地接续成功。三，人们能否抛弃对儒家的偏见。<br />
<br />
第一个方面我认为有成功的可能。认真读过论语的人都知道，孔子思想与个人主义和自由主义冲突的地方并不十分多，最被人诟病的儒家“等级”思想，其实细心想来也实在不是问题。因为“等级”就是一种关系和秩序，哪个社会没有？难的是对“利”的看法上如何形成统一。儒家轻利重义，什么“君子怀德&nbsp;&nbsp;小人怀利”、“君子怀刑&nbsp;&nbsp;小人怀惠”之类的，在儒家那里比比皆是。这种思想对市场的形成无疑是一大障碍。<br />
<br />
第二个方面，我对建构理性一直持怀疑，所以对人为接续儒家传统文化不看好。但，历史有时也的确是建构的结果，只要不是暴风骤雨般的强制，有意识的渐进的接续也未必就不成功。<br />
<br />
第三个方面，某些大众甚至知识分子对儒家偏见的祛除。目前看来比较难，但我相信，只要大家不要先入为主地看问题，而是耐心地读几本儒家原典（哪怕只读论语一部），就会发现孔子儒家并非我们原来批判的那么可憎可恶，其思想本身就蕴含着我们赞美和向往的观念。比如他身体力行的私人办学理念。多的我这里就不举了，夜深了，我得睡会儿，明天还要炒股。<br />
<br />
最后要说的一点是，目前的尊儒是中共倡导的，这可能让很多人反感，我也曾怀疑，他们这么做不外是用本土文化抵挡西化冲击。可是我也说过，我不敢完全排除中共在国家建设上确已有了更高的认识，而不仅仅是或根本不是抵挡西化。如果是这样的话，我们更应给它时间，让它用接续传统文化的办法、不显山不露水地替代共产这个舶来文化。何况这本身也符合渐进的理路。<br />
<br />
以上是我一个经济学出身的人的思考，难免有专业偏见和思维漏洞，有空再补充，欢迎批评讨论。<br />
