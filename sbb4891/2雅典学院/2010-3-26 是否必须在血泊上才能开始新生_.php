													<h2>是否必须在血泊上才能开始新生?</h2>
						
						
																											<div id="postmessage_533734" class="t_msgfont">对于17世纪的英国革命我们究竟了解多少？许多人只知道查理一世被砍头，随后双方达成妥协实现君主立宪；革命似乎四平八稳，从未出现法国革命的血腥场面，也没有拉瓦锡那样珍贵的大脑被送上断头台。这些都真实吗？<br />
<br />
如果你不止于道听途说，愿意从细节中挖掘英国革命的历史真相，请和我一起读一段“欧洲思想史”：<br />
<br />
在革命的第一次冲击中，贵族人文主义者只剩下最后一样武器，就是高贵地去死。保皇的贵族领主像波伊提乌和托马斯.莫尔一样，泰然自若地去死，“在神和世人面前做出榜样”。旧皇朝的最后一个卫护者斯特拉福德就在民众面前演出这样一幕戏景，他为自己进行了十八天的辩护，在此期间成为全国注目的焦点。他的演说词应当归入古老欧洲政治人文主义文献最伟大的部分。在其中最重要的一篇演讲中，他说：“各位勋爵，我们不要仿效先辈的坏榜样去练习杀人的艺术。”对新政权来说，这话正中要害，那些革命的领袖恰恰认为需要练习这种艺术才能使民众在这块新土地上站立起来。斯特拉福德和他的对手皮姆都尊重法律，反对恐怖和暴力，但斯特拉福德所理解的法律是为了建立自上而下的等级社会，维护王权的人文主义价值。由下面来的就是民众的无政府主义暴政。而皮姆宣告“法律就是国王”（Lex rex）时，他的思想恰恰相反，认为真正的拯救来自民众；至于专横，他认为贵族无视法律，掠夺民众的财产，草管人命，那才是专横。查理一世为斯特拉福德说情时说：“请考虑一下，良心是多么娇柔的东西！”几天之后艾塞克斯伯爵回答说：“议会就是国王的良心。”<br />
<br />
在第一次日耳曼思潮中，围绕爱克哈特，开始闪耀一个思想：人的心灵被神充满便是良心。在英国革命中，有宗教热诚的民众组成政治团体，正是他们的良心要求把老的领主和国王送上断头台。英国民众不宽恕国王在判决斯特拉福德死刑的判决书上签名，也不宽恕上议院中的二十二位主教在上议院最后投票判决时竟吓得不敢投票。国王和他的教会已经失去了由神而来的良心。<br />
<br />
在断头台上，斯特拉福德最后一次转过身来向民众说：“我请每一位听见我说话的人真诚地扪心自问一下：是否必须在血泊中才能开始新生？”<br />
<br />
这个向民众的良心的呼吁标志着旧世界的投降。斯特拉福德本来应该召唤天上、地上的一切力量来拯救他的。从那时起，一个新英国的心，主宰了国家，掌握了世上的权力。这颗心的起伏就是神的命令。黎塞留或许为除去斯特拉福德出了一份力量，但在听到处死斯特拉福德的消息时，他评论说：“英国人杀掉了他们中间最伟大的同胞”。<br />
<br />
<br />
以上是弗里德里希.希尔著“欧洲思想史”中的一个片断。这是一段多么惊心动魄的描述，历史和先贤向后人告诫了四点：<br />
<br />
1、&nbsp; &nbsp; &nbsp; &nbsp; 为使一个根深蒂固的旧世界退出历史舞台，不出现任何暴力的天鹅绒是一个神话；即使备受诟病的法国革命，其拿破仑战争也将欧洲封建体制连根拔起，为自由民主制度扫清了最后的障碍；<br />
2、&nbsp; &nbsp; &nbsp; &nbsp; 民众只有在一块新土地上才能站立起来；<br />
3、&nbsp; &nbsp; &nbsp; &nbsp; 宽恕的权利只属于胜利者；<br />
4、&nbsp; &nbsp; &nbsp; &nbsp; 不要失去赞美敌人的胸怀，对敌人具有的优秀品质甚至不要吝啬使用“伟大”这样的字眼；不要充满仇恨，为观念而战，不是为一己私利而战。<br />
