左愤开喷前看看这段话（转）<br />
<br />
1&nbsp;&nbsp;控制疼痛反射这样的原始反射的中枢并非大脑，而是脊髓。 也就说是剧痛之后，人做出的逃避动作是不受大脑控制的。 <br />
<br />
2 对疼痛的忍耐程度身上每个部位都不一样，黄种人头皮对疼痛的忍耐程度最高，缝头皮的时候不用局部麻醉。但是大面积烧伤就不同（邱少云只有两只手没有烧伤算起来烧伤面积90％左右）。 什么时候大家有空上灼伤科换药的时候来参观一下，听听病人们怎么鬼哭狼嚎。用意志力控制也是不可能的躲避疼痛。这么原始的反射是通过脊髓进行的而不经过大脑。比如你的手不小心碰到了火烛第一是缩回手（颈部脊髓下的命令）第二步才会感觉到疼痛（大脑中央后回感受中枢接受到了传导过来的刺激）。<br />
<br />
3 高级中枢控制低级中枢是普遍现象比如排尿反射 ，排便反射正常人在一般情况下都可以控制，但是在突发情况下遭到的强烈刺激，一般都由通过感受神经末梢－感觉神经元－脊髓前脚－脊髓后脚－运动神经元－肌肉。这个反射过程，不通过大脑皮层 。<br />
<br />
4 感到疼痛是大脑皮层的中央后回的事情手躲避疼痛 应该是颈髓作出的反应，两者不是一个地方的工作。<br />
<br />
5 这些说法的依据是建立在这样一句话上面的 “英雄是由特殊材料构成的”。 邱少云和我们一样都是人，不可否认从经验看，黄种人对疼痛的忍耐程度比白种人高不少，但是他一样不可能突破自己身理极限。说得白点我们忍不了的事情他也忍不了。大面积烧伤的病人，包括已经昏迷甚至去脑强直（大脑皮层失去功能）的病人，都会作出强烈的反应。所以对邱少云的事迹表示一点怀疑 <br />
<br />
6 总结看法是这样的邱少云在一开始就已经死了 ，理由是这样的：官方说法中 邱少云是被活活烧死的也就是说清醒－&gt;死亡。实际人的烧死的过程并不是这样的。在一个死亡的过程中，还要经过昏迷的过程。昏迷的过程也分成几个阶段--至少3个阶段。如果细分一点可以分为10个阶段以上，邱少云的死亡过程应该是清醒－&gt;浅昏迷－&gt;中度昏迷－&gt;深昏迷－&gt;死亡。深昏迷的时候人的大脑皮层严重受损是无法活动的。可是，浅昏迷和中度昏迷的时候，人是可以动的！ 但是丧失意识，无法控制。<br />
