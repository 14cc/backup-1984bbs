作者：三妹<br />
<br />
<br />
<br />
<br />
我的家庭历史就是中国共产党历史的剪影，它折射出当时迷茫的知识份子追求真理的狂热和盲目，以及共产党打着共产主义旗号夺取政权的过程。　　<br />
<br />
我的奶奶一九二零年进入北京大学学习。当时全中国的大学都不收女生，但那年奶奶正巧赶上“开女禁”的风潮，因此，北大接收了九名女子旁听生，奶奶是其中之一！<br />
　　<br />
奶奶在北大最初结交朋友中的两个人引导她接受了共产主义思潮，一个是信仰共产主义、后来成为中国共产党创建人的张国焘，一个是研究社会主义的宋维民。这两个朋友在奶奶一进入北大后就带奶奶去拜见了把共产主义思想介绍到中国的主要推手李大钊教授。从此奶奶成为李大钊的入室弟子，并于一九二六年加入中国共产党，奶奶全家也成了中国共产党的北平地下联络站。　　<br />
<br />
日本占领北平后，奶奶家的共产党地下联络站主要负责把北平的左倾学生和药品送往晋察冀和延安的共产党领区，奶奶也在一九三九年把我的两个姑姑送往晋察冀共产党领区，当时大姑姑十七岁，小姑姑才十五岁。一九四零年初，共产党组织通知奶奶，她的地下联络站已经不安全，她必须尽快离开北平去晋察冀。三天内，奶奶抛弃北平的家带着我的十九岁的父亲去了晋察冀。两年后，奶奶又带着我的小姑姑从晋察冀转移到延安。内战时，共产党全部撤出延安，奶奶随着中共中央组织部的队伍回到河北定县。奶奶不愿意住在疗养所无所事事、饱食终日，她便要求去定县中学教书，重操旧业。此前，日本一投降，我的爸爸就被晋察冀军区联络部派遣到石家庄打进了国民革命军第三军军部，他名为国军第三军军部文书实为八路军特工，在国军第三军军部隐藏了近一年，最终被叛徒认出而连夜逃出石家庄跑回晋察冀军区联络部的驻点城南庄。后来，爸爸因病重来到奶奶任职的定县中学修养。　　<br />
<br />
下面讲的是我爸爸到定县中学养病时遇到我母亲的故事，从这段故事，我们可以看到中共搞运动整人的历史片段。<br />
<br />
一见钟情 （一九四七年）　　<br />
<br />
定县中学是河北历史最悠久的中学之一，始创于清乾隆三年（公元1738年）， 一九0二年由“定武书院”改为“定州官立中学堂”，后几经易名。早在一九二九年， 共产党就在定县中学的前身省立第九中学建立了中共地下党支部，这个党支部属定县最早的共产党组织。　　<br />
<br />
中共在定县设立了河北省教育厅，教育厅对享有盛名的定县中学非常重视。虽然中共在内战期间还不能控制大学，但是已控制了不少中学，并不断地从部队调派知识份子进入各个中学任校长和教师，不少部队的知识份子也主动要求复员到学校任教。奶奶任教的定县中学就搀进不少共产党派去的教师。　　<br />
<br />
爸爸在一九四八年也复员去学校工作了，他复员却完全是为了爱情。<br />
　　<br />
自从一九四六年夏爸爸从国民革命军第三军跑回晋察冀地区联络部后，便陷于繁忙的文字、联络、通讯等工作中，他积劳成疾，得了严重的痢疾，常年拉肚子，随后又得了严重的痔疮。拖了一年后，他的身体彻底垮了，完全不能工作了，军区联络部给爸爸长假让他去定县中学的奶奶处养病。　　<br />
<br />
一天，奶奶在屋里听到外面有动静，便走出门看，只见几步远 一个穿着一身旧军装的、骨瘦如柴、形如骷髅的年轻人叫了声“娘”，就坐在了院子的石凳子上。 奶奶没想到儿子竟病得连自己这个当娘的都认不出来了。　　<br />
<br />
到定县中学第二天爸爸就住进了位于唐县张各庄的中共中央附属医院，两个星期后爸爸病好出院，回到奶奶家疗养。医生的意见是，待身体彻底恢复后，就做痔疮手术。良好的医疗和奶奶周全的照顾使年轻的、生命力旺盛的爸爸很快就康复起来，照奶奶的话，病好后的爸爸吃起饭就像小老虎。　　<br />
<br />
养病期间，爸爸还帮奶奶去代课教国文，教了几次课便在学生中产生轰动。他教书出色的名声还传到了河北省教育厅，单身的爸爸很快就成了当地众多女教师追求的对象。那时，他在定县中学与一位音乐女教师交往最频繁。　　<br />
<br />
这位音乐女教师出身于上海大资本家家庭，五岁开始学钢琴，音乐造诣很高，还是圣约翰大学毕业的大学生。抗战后期，她背叛家庭跑到晋察冀根据地投奔了共产党，后来到定县中学教音乐。她挑选出适合爸爸演唱的英文歌，俩人一奏一唱，配合得天衣无缝。爸爸最得意的是，他可以边看着五线谱边唱出刚拿到手的新歌。爸爸运用自如的浑厚的男中音，迷倒学校所有的教师和学生，也迷住这位音乐女教师。可是，爸爸却感到，他对音乐女教师的感情却只是友情而已。　　<br />
<br />
外校的一个年轻女教师也常来找爸爸一同出游、看电影。她是由教会的美国牧师养大的孤儿，能说一口漂亮的英语。奶奶很喜欢她，说她漂亮贤慧。可是，爸爸感到对这位女教师的感情也仅仅是友情。　　爸爸是个夜猫子，有熬夜读书的习惯。一天半夜，全校最漂亮的女教师敲门进到爸爸的屋子里来，还没说几句话，她就娇滴滴地坐在了爸爸的腿上。爸爸二话没说，几下就把她推出门外，告诉她以后不要再做这种傻事。　　<br />
<br />
爸爸对这些女教师不能产生热情，总感到一种莫名的距离感。奶奶看到已经二十六岁的儿子这样挑剔，不禁开始担忧起来。这时，爸爸竟意外地遇到我的妈妈， 感情立刻热烈得一发不可收拾。　　<br />
<br />
那是一九四七年八月底学校放暑假期间，中共河北省教育厅将在定县召开教育大会，周围一带共产党掌握的中学的部分教师在此聚集一堂。奶奶让休病假的爸爸代她去开会，爸爸本不想去，但是奶奶劝他去听听，说可能他还会遇到许多从晋察冀军区复员到各中学工作的战友。　　<br />
<br />
大会总共开三天。爸爸到大会的第一天，真的欣喜地见到不少战友，爸爸和他们一起坐在了会议的前排。大会开始前，爸爸回头看看后面是否还有熟人，看看有多少人来开大会。没想到，爸爸这一回头就一眼看到门外走进一个貌美而清瘦的女教师。　她有着一双明亮的大眼睛，一对长长的秀眉弯弯地勾住大眼睛，份外醒目，更令爸爸动情的是她眼中那份难以名状的哀伤。爸爸当时就呆在那里，眼睛一刻不能离开这位女教师。一位战友在爸爸身边酸溜溜地操着浓重的平山腔音问道：“共子，看啥哩？看风景儿呢？”　爸爸这时已经不能专心地开会，只看到台上发言的人嘴在动，可不知道他们都在讲些什么。　　<br />
<br />
大会休息期间，大家都聚在乒乓球室打乒乓球，那时很少有女的会打乒乓球，妈妈走进去要求打一盘。这时妈妈看到一位身材魁梧的年轻军人走过来，提出要跟她较量一下球技。于是，俩人上下开战，打得难分难解，赢得满室围观的人叫好。爸爸发现对方是很有运动特质的女性，她叫罗理，曾是北京师大女附中的高材生，后考入西北大学法商学院商学院，一九四六年夏到张家口投奔共产党，在华北联大政治班学习了一年，现在定县东南一百五十里外的辛集中学教书。　　<br />
<br />
妈妈告诉我她刚见到爸爸时对他的印象。妈妈说：“那时正是战争年代，食品供应极为短缺，大家都很瘦，只有你爸爸被你奶奶喂得很壮，一头浓密的的黑发，长鼻子，扫帚眉，吊眼儿，活像一只老虎，他光着个大膀子在篮球场上拚命地跑来跑去地跟人抢球。”当时爸爸不敢多问妈妈有关她的个人问题，他在大会当场抓了个空跑回定县中学求奶奶帮忙。一见到奶奶他就焦急地说：“娘，你一定要帮我打听清楚她的个人情况，有没结婚。”奶奶后来给我讲爸爸这段故事时说：“我从没见到过你爸爸那失魂落魄的样子。”<br />
　　<br />
第三天大会结束，大家都互相道别，各自回自己的学校了。妈妈也走了，爸爸突然感到从来没有过的若有所失的怅惘。　　爸爸从教师大会回到定县中学后，奶奶告诉他：“罗理不但已婚，还有孩子，夫妻均是西北大学的学生，一年前双双来到根据地。”爸爸听到妈妈已经结婚，大失所望。从此以后，爸爸便心神不定，眼前总是浮现出罗理清瘦亮丽的面容和那双哀伤的大眼睛，那哀伤的眼神后面分明藏着无限的痛楚，尤其那极为清瘦的面容使爸爸感到，只有经受了巨大精神折磨的人才会如此瘦弱。爸爸不由得联想到前一阵 “查阶级、查思想、查作风“的“三查”整肃运动造成多起自杀事件，爸爸又联想到可能其中一个自杀身亡的大学生是罗理的丈夫。　　<br />
<br />
几天后，一个星期六下午两点左右，妈妈正独自在学校，有人告诉她说，有朋友来看她。妈妈正疑惑着谁会来找无亲无故的她时，爸爸欣喜地走到了她的面前。<br />
<br />
妈妈惊奇地问：“你怎么来啦？是不是还想跟我打乒乓球？”　　爸爸有些激动地说：“我来看看你，有重要事要跟你谈。”妈妈看到他满鞋、满裤脚的尘土，才想到从定县到辛集一百五十多里路哪，就问他怎么来的。他说， 骑自行车来的。妈妈赶紧请他到屋里坐，给他打了一盆洗脸水，说：“先洗洗脸吧，吃饭时间也过了，你是远道来的客人，我这里什么吃的也没有。”妈妈突然想起来，她腌了一小缸鸡蛋，由于放盐少了，鸡蛋有点臭味。于是问他：“你喜欢吃臭鸡蛋放香油吗？”他说：“很喜欢吃，你有吗？”妈妈就去食堂煮了一碗臭鸡蛋，买了几张大饼。爸爸用大饼卷臭鸡蛋，狼吞虎咽地吃起来，吃得很香。　　<br />
<br />
爸爸在老年时还深情地向我描述了那天他第一次看到妈妈的宿舍和她的床铺的那种新鲜感觉。他说，他从没见过那么洁白清馨的床铺：白床单、白枕巾、白蚊帐，床上还放着一个白磁缸。妈妈请爸爸坐在椅子上，自己坐在了床边，爸爸感到，那个象仙境一样纯净洁白的床把妈妈衬托得就像一朵圣洁的白百合花。爸爸还说，他以前从没见过像妈妈那样小巧洁白的整齐牙齿，他见到妈妈后才惊奇地发现，人的牙齿竟能天生成如此的精美整齐。　　<br />
<br />
爸爸吃完饭后，妈妈说： “你骑车骑了这么远的路，一定累了，还是睡一觉休息一下吧。”爸爸说：“还好。”妈妈想起乒乓球桌子，现在正是放暑假，没有人打球，就问：“你可以在乒乓球桌子上睡觉休息吗？”爸爸说：“可以。”妈妈给爸爸铺好乒乓桌，爸爸一躺下来就像散了架一样沉沉睡去。　　<br />
<br />
爸爸睡醒后，俩人到街上散步，爸爸突然问妈妈结婚没，妈妈说：“不但结了，还有一个儿子。”爸爸说：“我知道，你丈夫死了。”妈妈一惊，问：“你怎么知道的？是不是背后有人议论我的身世？”爸爸说：“不是，是我自己猜的。在‘三查’运动中有一个大学生经受不住严酷审查而自杀了，我猜想他是你的丈夫。”妈妈说：“那一天有三个人自杀，你怎么知道其中就有我的丈夫？”爸爸说：“我观察你的言谈举止一身学生气，又如此瘦弱，就猜到了，我这次来就是证实我猜的是否正确。”妈妈问：“你是什么意思？”爸爸这时红着脸说出了憋在心里好几天、天天都演练着想对妈妈说的话，他说：“我爱你。我见的女学生很多，从没有动过心，见了你以后时刻想你，离不开你了。” 丧夫不久、惊魂未定的妈妈，听到爸爸这么说有点儿慌，就说：“别说了，你不了解我现在的处境，如果你知道了，就会躲我远远的了。”这时，他们走到一个小饭铺门口，便进去吃了点东西，吃完饭俩人又回到学校。<br />
<br />
妈妈看天色不早了，就说：“你还要走远路呢，再不走天就黑了。”爸爸说：“我刚来这，跟你还没说几句话，你就赶我走，太不客气了吧。我今天不走了。今晚我干脆就在乒乓球桌上睡一夜，明天是星期天，我明天再走。”妈妈更慌了，不知怎样面对爸爸炽热的感情，就推托说：“明天我没时间，明天我还要到村里去看孩子呢。” 这话正中爸爸下怀，爸爸兴奋地说：“明天我和你一起去看孩子！”　　<br />
<br />
第二天一早，在朝阳下，两个年轻人兴致勃勃地一起去看妈妈寄养在附近村里老乡家的孩子。一路上俩人慢慢地聊着边走边谈，大部分时间都是爸爸说，说他的年龄，工作，家庭情况，说他现在正在母亲处养病，病好后再回部队，等等。妈妈听着爸爸讲，心里却不断掂量着，思绪乱极了。妈妈心想：只打了一次乒乓球就交了一个陌生朋友，会不会给自己招来麻烦？但是，爸爸毫无城府的学生气和自然流露的纯真善良让妈妈感动。另外，俩人同是北平学生。共同背景又使妈妈对这位年轻的军人有亲近感。妈妈告诉爸爸，她给孩子起名叫慰慈，是安慰妈妈， 待人慈善的意思。　　<br />
<br />
走着走着，俩人渐渐地像好朋友一样一同唱起共同熟悉的歌曲，爸爸这时忍不住开始炫耀自己的好歌喉，唱完英文歌曲，又唱京戏。出乎爸爸意料的是，妈妈也会唱京剧，她唱的青衣有板有眼，颇有功底。　　<br />
<br />
原来，妈妈有个年长她七岁的大表哥叫罗晶，罗晶的外祖父是天津京剧名角德君如，德君如曾被西太后慈禧召到皇宫里唱戏。罗晶从小深受外祖父德君如的影响，不到二十岁便在天津一个京剧戏班里作了小鼓手。罗晶看到这个表妹貌美，一心想把她培养成京剧名角，从她十岁起他就开始教她唱戏、炼功。　　<br />
<br />
爸爸看到妈妈也懂京剧，更兴奋不已，他唱着京剧又走起台步来，妈妈也像孩子一样地兴奋起来。妈妈会跳舞，也忍不住给爸爸表演了一番。然后，妈妈不好意思地停下舞步说，别跳啦，让人家看见，以为咱俩是神经病呢。爸爸才不管别人怎么看呢，他能和妈妈在一起别提多高兴了，两个年轻人就这样又唱又跳地走进了村子。他们一进村，就见村民们兴奋地叫喊着奔走相告：“大嘴的爸爸、妈妈来看大嘴啦！大嘴当八路的爸爸来啦！” 村民们搞不清这个寄托在村子里的孩子的名字和家庭状况，看到孩子嘴大就给他起名叫大嘴，看到妈妈身边有个穿军装的男人，就想当然地认为是孩子的爸爸。　　<br />
<br />
爸爸和妈妈到老乡家看望了孩子，还一起给孩子洗了澡，看到孩子的奶妈是个聋子，爸爸很不放心。在回学校的路上，他对妈妈说：“把孩子送出去不是办法，应该自己看管。”妈妈说：“我要工作，哪有时间带孩子”爸爸说：“回去我想想办法。”　　下午，爸爸向辛集中学校长谈了妈妈的困难，要求学校给妈妈分一间房子，把孩子接回来找保姆照管，这样罗理既可以每天看到孩子，又可以安心工作。校长当时就满口答应，马上就给妈妈腾出一间存放杂物的库房。第二天，爸爸和妈妈上街去买了一张大双人床，学校的同事当天就在当地找到一位六十多岁的保姆来照看孩子。这位保姆曾在大城市工作过，有带孩子的经验，还会做饭。妈妈兴奋不已，说：“慰慈可要享福啦。”　　一切都安排妥当后，爸爸对妈妈说：“我出去一下，明天就回来。”妈妈这时对爸爸已生依恋之情，心慌地问：你要去哪儿？干什么？”爸爸说：“你不要管了。” 说完骑上自行车就走了。　　<br />
<br />
妈妈担心地过了一夜，猜不出爸爸去干什么了。第二天早上爸爸满头大汗地推着车回来了，车上大包小包玲琅满目，一个红色的双钱牌大暖水瓶，两桶美国军用奶粉，孩子的小衣物，还有许多精美的饼干和点心。　　<br />
<br />
妈妈惊奇不已，辛集这个穷乡僻壤，哪来这么多高级物品和食品。妈妈问爸爸从哪里搞来这么多东西。爸爸说：“我去石家庄了。”　　<br />
<br />
爱情的力量真是伟大，为了给孩子买衣物和食品，爸爸仅仗着自己对石家庄情况了如指掌竟进入仍是国民革命军第三军罗厉戎部队驻守的石家庄。他忍着痔疮的痛苦，两天内来回骑车三百里路，而且还穿着一身八路军军装。　　<br />
<br />
妈妈心疼地说：“你穿着这身衣服去石家庄，就不怕敌人把你逮捕。”爸爸笑着说：“我要不去，慰慈可就吃不着奶粉了。现在一切都安排得还可以，你满意吗？” 妈妈说：“满意，谢谢? 。”爸爸说：“只说谢谢就行了吗? ”妈妈说：“那还要说什么？”爸爸说：“你闭上眼，把手给我。”爸爸抓住妈妈的双手围在自己腰上，捧住妈妈的脸热情地亲吻起来，妈妈情不自禁地深深陶醉在爸爸的热吻中。爸爸深情地看着妈妈说：“把孩子的名字改成慰慰吧 ，现在他不只安慰你，也要安慰我。”　　<br />
<br />
爸爸接着说：“我现在要回去治病，然后考虑我以后的工作问题。”妈妈关心地问：“你有什么病？”爸爸说： “不是什么大病，我有痔疮，累了就便血。”<br />
　　<br />
妈妈叫起来：“天啊！这些天是不是把你累坏了，你怎么不早告诉我？还骑自行车。 不行，你不能走，这里也有医院可以治病，你就在这里的医院看病吧。”爸爸说：“我还是回去，说不定要动手术。”妈妈说：“我跟你去，我在医院陪你照顾你 。”<br />
　<br />
爸爸说：“这是小手术，不用住院，很快就会好的。你等着我，回去后我就给你写信，你放心好了。”　　<br />
<br />
这时，爸爸妈妈两颗心已经紧紧相联再也不能分离。妈妈终于向爸爸吐露了长久压在心中的伤痛，她给爸爸讲了自己的身世，还讲了丈夫怎么受共产党组织的审查整肃和自己刚生下孩子第六天丈夫就自杀的悲惨经历。爸爸流着泪听完妈妈的故事，把妈妈紧紧地搂在怀中，发誓他再不会让妈妈受到伤害。　<br />
<br />
三十九年后的一九八六年八月，在我离开北京赴美国的前一夜，妈妈与我睡在同一个床上，谈了一夜。妈妈讲了他的身世，也同样讲了她和前夫受共产党残酷整肃的故事。<br />
　　<br />
大卢之死　　<br />
<br />
妈妈的祖父是江西九江人，经商落户在河北省天津市，因在天津经营煤矿而发财。祖父生有三儿三女，三个儿子都不善经营，吃喝嫖赌，挥霍无度。　　<br />
<br />
妈妈的父亲是祖父的二儿子，他不务正业，沉溺于嫖赌之中。妈妈的母亲是个非常要强的女人，通过刻苦自学能读书写信，家务事更是高手，会刺绣编织，会做中、西式套装，还能做中西餐整桌酒席。凭着这些手艺，她曾在女子职业学校任教，但是微薄的收入不够丈夫挥霍。她长期不满丈夫在外吃喝嫖赌，一直指望自己能生个儿子今后有依靠。一九二一年一月的隆冬季节，她却生下了个女儿──我的妈妈。<br />
　　<br />
产后六个月的时候，夫妻不知为何缘故大吵一架，烈性的她一气之下服毒自杀，妈妈的父亲也离家出走、不知去向，妈妈从此成了孤儿。妈妈的大伯父领养了她，把她交给了自己的二太太抚养。妈妈的大伯父有妻妾三人，大太太虽然没儿没女，但属明媒正娶，在家有地位； 三太太生子有功，在家里也有一席之地；唯独抚养妈妈的二太太既不能生育，又不是明媒正娶，地位低下。　二太太夫家姓罗，娘家姓褚，叫罗褚氏。罗褚氏是清朝大官褚天佑的独生女儿。褚天佑受到清朝相国李鸿章的重用，曾数次任沪海关监督，又称海关道，在天津很有声望。不幸的是，在罗褚氏十五岁时，褚天佑去世，罗褚氏的母亲抽大烟耗尽家产，最后不得已把独生女儿卖给罗家做妾，自己也跟着进了罗家寄人篱下。罗褚氏从小娇生惯养，父母都舍不得给她缠脚，只是把她的脚稍稍缠紧不让自然长大，并不伤到脚骨，她的脚后来变成了半大小脚。罗褚氏是出名的与世无争的好心人，嫁到罗家后，亲戚、邻居都叫她“好妈妈”。罗褚氏后来成为我的姥姥，为照顾我们兄妹五人付出了巨大精力。　　<br />
<br />
自从收养了妈妈为女儿，罗褚氏乏味的生活有了乐趣和慰藉。罗褚氏和她的母亲对妈妈百般疼爱，她们看妈妈像玫瑰花一样美丽，给妈妈起了小名叫瑰瑰。可是她们不重视女孩的教育，直到妈妈九岁时才给她起了学名罗炜，让她进了小学。　　<br />
<br />
一天，妈妈看见她的大伯母洗脚，大伯母那双畸形的脚把妈妈吓坏了。大伯母脚上有很多伤疤，只有一个大脚指是直的，其他四个脚趾全都弯曲折断贴在了脚底下，脚背也折断弯成球状。大伯母洗脚要用一个多小时，先用一盆温水泡，然后换第二盆热水洗干净，之后剪指甲、刮脚皮，将全脚修理光滑，接下来，再用自制的白矾粉涂在脚缝里，再用二寸宽数尺长的浆好的白布条一圈一圈地将脚层层地裹得紧紧的，用针线缝好，最后把裹好的脚穿进特别定做的袜子和鞋里，成为三寸金莲。大伯母定期到一个老年鞋店定制木底鞋，鞋店按照脚形在木板上刻下大伯母的鞋底，大伯母一次要定做好几双木底鞋。　　大伯母告诉妈妈说：“孩子，你生在好年代了。以前女人全是男人养活，是男人的玩物。我三岁多就开始缠脚，反抗就挨打。我哭着问妈妈为什么让我的脚痛啊？ 妈妈说短痛比长痛强啊，你不缠脚以后长成大脚丫就没人要，小脚好看人人爱。我问妈妈你也不要我吗？妈妈说，我想要你也没能力养活你呀。为了长大有男人要，三岁时家里就把我的脚骨缠断。最受罪的是缠完脚后还要用磨豆腐的石头磨盘压住两只脚，撕心裂肺地痛啊，两条腿也连着痛，压完脚就站都站不起来，根本走不了路了。再长大些以后，看见人家小脚穿着绣花鞋走路好看，害怕自己没人要，就跟妈妈学会了缠脚的方法，也拚命地把自己的脚往小里缠，小脚成了我们这代人一辈子唯一的追求。孩子，你可不要像我们，你一定要上学，女人有了文化就能工作养自己，不靠男人。”大伯母的畸形小脚强烈地刺激了妈妈幼小的心灵，大伯母的一席话使妈妈明白，她今后一定要成为有文化、独立的女人。　　<br />
<br />
一九三六年，妈妈小学毕业时，家里已经败落，穷得都不能供妈妈上中学了。 这时，妈妈听说自己的伯父已经续弦再婚，移居到北平，她决定离开天津去北平投靠亲生父亲，要父亲供她上中学。　　<br />
<br />
妈妈来到了北平见到了从未蒙面的父亲。这位不负责任的父亲见到妈妈第一面就给她订出 “约法三章”：第一，要考上北平的好学校。第二，考上好学校后要争取成绩前三名，这样可以免交学杂费。第三，要帮后妈做家务事。　　<br />
<br />
妈妈在家休学一年，一面在家复习功课，一面帮继母做家务。第二年，妈妈果然如愿以偿，考取了北平师大女附中。上学几年来，妈妈每天早晨负责做早餐，放学回家后就帮继母做晚饭，饭后刷盘洗碗打扫卫生，做完家务才温习功课做作业，每天都要到十一点才能睡觉。在初中几年中，妈妈的成绩一直都是前三名，初中毕业后被保送高中。　　<br />
<br />
妈妈为了获得学校运动会体育金牌而刻苦参加体育训练，成为历届全校运动会的十项全能冠军。全校运动会颁发的金牌含真金，妈妈将其卖掉换钱以补贴生活。 直到妈妈和爸爸结婚时，她还存有一饼干盒子的奖牌，爸爸把它们全卖了，买衣物、 下馆子，很快就花光了。　　<br />
<br />
妈妈上中学二年级时，北平已经沦陷。上高中时，一位共产党地下党员的历史教员常常以平等、大同等共产主义理想来影响学生。妈妈班上有三分之二的女同学在他的影响和引导下，不惜中断学业、离开家庭投奔延安或晋察冀的共产党领区， 其中有投奔延安后嫁给八路军高级将领林彪的叶群。　　<br />
<br />
妈妈的生父告诉妈妈说，她是在东北出生的，当时那里正在枪毙红胡子（东北话称土匪），枪刚一响她就出生落地。家里人都说妈妈是红胡子托生的，天不怕地不怕。果然，妈妈高中毕业后就真的远走高飞了。　　<br />
　　<br />
一九四二年，妈妈高中毕业。妈妈把自己的自行车卖掉当路费，离开北平，抱着参加抗日的决心，沿着同学相告的路线往晋察冀走。到了河南洛阳，妈妈住进“战干团”的学生招待所，并准备先在“战干团”参加抗日工作。“战干团” 是国民政府军事委员会“战时工作干部训练团”的简称，是抗日战争时期国民党中央开办的一个大型军事、政治训练机构。　　<br />
<br />
妈妈遇到正在 “战干团”工作的同学戴静宜，戴静宜告诉妈妈，当地的“战干团”非常黑暗、腐败，一些满怀抗日热情的女学生在此地工作却遭到“战干团”官员的奸污，受害的还有几个北平师大女附中的同学。戴静宜劝妈妈不要在“战干团”工作，还是继续求学好，她说这里根本没有什么抗战工作可言。妈妈听后感到意外和失望。就又卖掉一些物品作路费，离开“战干团”招待所，继续向西北方向走。　　<br />
<br />
走到汉中时，妈妈在城固县又遇到一位同学，从她口中得知，这里的西北大学的食宿都是公费，成绩好的学生还可申请到奖学金。妈妈听后便去应考，结果顺利考入西北大学法商学院商学系。　　西北大学肇始于一九0二年，创建于西安，始称陕西大学堂，一九一二年改名为西北大学；一九二三年八月改称国立西北大学。一九三七年抗日战争爆发后，为躲避战火，北平天津的三所高校──国立北平大学、国立北平师范大学和国立北洋工学院──迁至陕西，与西北大学组成国立西安临时大学。一九三八年三月，国立西安临时大学迁往陕西省城固县，同年八月八日，国民政府教育部决定将国立西北联合大学改为国立西北大学。西北大学当时设有文、理、法商三个学院，共十五个系， 有师生约一千五百多人。　　<br />
<br />
西北大学文理学院在城固县城，妈妈所在的法商学院在城固县郊区。 当时西北大学没有学生会之类的自治组织，系会是学校官准的合法组织之一。妈妈在三年级时被班上的男同学一呼百应地选为系会总务。其实，同学们都对系务活动毫无热情和兴趣，妈妈在任内只出过两次稿，每次稿子都要事先送到国民党党部审查，结果有的稿子被删得乱七八糟，有的稿子整篇被扣留，做事认真的妈妈对校方的做法非常生气。　　<br />
<br />
和妈妈一起办事的本班男同学卢贵英给妈妈讲了校方的许多黑暗情况，告诫妈妈要忍耐，否则会被扣上共产党份子的帽子。他们俩人马马虎虎地搞了两次活动就再也不搞系务活动了。　　自办活动以后，妈妈开始与卢贵英经常来往。卢贵英生于一九二一年七月，比妈妈小六个月，他是唐乐亭一家有名的大地主的独子，家中极为富有。卢贵英身上有着许多传统家庭教养出的好品德，他为人正直，待人诚恳豪爽，做事努力认真，是一个极为刻苦钻研的学生。他英文造诣极高，博览群书，思想开放，还通晓经济理论，很受同学们的尊敬。他身材高大，大家都亲热地叫他大卢。　　<br />
<br />
大卢经常和妈妈谈论一些共产党的主张和苏联社会情况，以及社会科学和政治经济学理论等话题。他讲的很多东西对妈妈都是全新的，大卢使妈妈思想、眼界大开，俩人不久遂陷入热恋。　　<br />
<br />
当时西北大学的共产党组织非常活跃，他们深知外表、能力和学业出众的同学在年轻学生中起到的影响和鼓动作用。一九四五年妈妈上三年级时，西北大学地下共产党组织开始有意接近妈妈，共产党员杨丹（四年级经济系学生）成为与妈妈单线联系的联络人。在他的影响下，妈妈很快便成为激进的左倾学生。当时，杨丹劝妈妈加入共产党，妈妈却没有接受。妈妈在自传中写道：“（我）另一方面也经常听到一些不利于共产党的传言，自己对之尚无力辨别，因此对共产党还存有疑虑。”　　<br />
<br />
一九四六年二月初到五月，由共产党在背后策动，由学生领袖在前面号召，西北大学开展了一个轰轰烈烈的、震动全国的学生运动。妈妈从学运一开始就是出头露面的学生领袖，大卢也积极地参与了这次学运。　　<br />
<br />
西北大学这次学生运动是由于学生当局要召集反苏游行，妈妈提出不同倡议而发生争执引发的。结果最后发展成学生自治会强行接管学生政务，学生无限期罢课的大学运。　　<br />
<br />
一九四六年春，学校当局召集各系总务开会布置反苏游行，妈妈出面提出两条不同意见：一，系总务只能在校方与学生之间起传达作用，不能替代同学决定事情； 二，是否举行游行必须征求同学意见，校方不能强奸民意擅自决定。　　<br />
<br />
校方让妈妈等人回去和各班同学商量。妈妈随后召集各方代表商量作出决议： 首先成立学生自治会，然后由学生自己的组织决定游行等事务。校方对学生的决议则以“考虑一下”来推脱，暗中却通过三青团系统及其团员直接在同学中组织游行。　　<br />
<br />
妈妈和一些同学当即召集全校学生大会，说明了上述经过，提出成立学生自治会，并马上选出四位学生代表：罗炜，卢永福、王庆新、杨丹四人。妈妈罗炜是唯一的学生代表。　　<br />
<br />
四代表出面与校方交涉要成立学生自治会。教务长杜元载出面说，校长现在不在学校，成立自治会的事不能决定。然后威胁说，如不游行，学校有学校的办法， 如捣乱，学校也有办法对付等等。四代表把教务长的话向全体同学通告后，引起同学们的义愤。　　<br />
<br />
妈妈向同学们呼吁：“我们对校方必须要有强硬的表示才行。”同学们大声回应，一致同意罢课两周。校方得知罢课消息后，发出开除四代表的布告。同学们看到布告后纷纷拥至教务处，要求校方撤销开除四代表的决定。校方迫于压力，收回开除的命令，改成 “留校查看”。学生四代表仍继续领导罢课，坚持要求成立学校自治会。　　<br />
<br />
三月一日，校方召集了全校各院长、系主任及各班学生代表参加的师生联席会议“共同商讨爱国游行”。学生代表表示，自治会不成立，就不参加游行。当即，学生便自行成立了“学生自治会筹备会”，抵制校方组织的反苏大游行。当晚，学生自治会筹备会召开了全体同学大会共同商量对策。　　此时，教务长杜元载与在外地的校长刘季洪通电话作出了三项决议：一，学生四代表罗炜、卢永福、杨丹、王庆新鼓动罢课，破坏校纪，立即开除学籍；二，学生自治会准予成立，惟需循合法手续，民主方式，成立关生正式组织后，现有的学生自治机构着即解散；三，限于三月十二日一律复课，倘有故违者，严惩不贷。　　<br />
<br />
三项决议公布以后，学生纷纷提出抗议。学生自治会干事会贴出公告，抗议校务会的决议，要求校方收回成命并号召全体同学无限期地罢课。同时，自治会决定改选，刚从西安请示地下共产党组织回来的政治系四年级的学生王佐臣选为自治会领导。妈妈罗炜也是改选后的自治会成员之一。　　<br />
<br />
校长刘季洪从外地回来后，以期中考试和毕业临近为由，组织三青团暗中拉同学们复课。同时，校方再次公布开除前四代表，还开除了新选出的全体自治会成员和另外十几位同学。这下子学生们被激怒了。　　<br />
<br />
四月十五日，学生自治会召开全体学生大会，一是庆祝自治会改选的胜利，二是请刘季洪到会表态，承认改选后的学生自治会的合法性。刘拒绝出席，并指派一群学生拿着棍棒捣乱会议。自治会当即成立纠察队，纠察队带着同学们一拥而出，去找校长刘季洪算账，校长刘季洪闻风从后门逃跑了。学生决定接管校政，把刘季洪赶出学校。此时，有同学来报告说，刘季洪正在校内偷偷开会，准备抓捕学生。同学们即刻找到刘季洪开会处，一进门就缴下了警卫和校警的枪，校长刘季洪气得拍着桌子对学生大喊：“你们闹吧！你们闹吧！”同学们围住刘季洪，一致要求：承认自治会的合法地位，收回开除自治会成员的错误成命，“恭请”校长离校。　　<br />
<br />
随后，学生自治会以校方的名义分别草拟了三个布告，随后盖上了从校长秘书手中夺来的钢印，当即将布告贴在校长布告栏内。　　<br />
<br />
由学生自治会拟出的盖着校长大印的三个布告是：<br />
　　布告一：“国立西北大学布告：本校学生自治会现已组成就绪正式成立，合行布告周知。　　此布 校长 刘季洪 民国三十五年四月十五日”。<br />
　　布告二：“恢复开除学生学籍的布告”。<br />
　　布告三： “本校长自本月十五日起离校”。<br />
　　这天，刘季洪离开了学校。<br />
　　罢课期间，食堂的伙食比平时的还好，四菜一汤。妈妈抓住吃饭时间在大食堂演讲，抨击校政腐败，指着四菜一汤对同学们说：“同学们，校方是在用好菜好饭收买我们就范！我们不能上当受骗！”　　<br />
<br />
自从学生自治会接管校政以后，校长刘季洪便几次到警备司令部交涉，要求出动军队协助学校镇压。此时的政界对学生运动都谨慎行事，不敢轻易采取镇压行动， 因为，去年底云南警备总司令关麟征镇压昆明“一二・一” 学生运动而被 “停职议处”。　　<br />
<br />
刘季洪又直接给国民政府教育部密电，说学生枪夺校印，搞武装暴动，说学生要放火烧校本部，说学生的过激行为将危害周围的商店、住户以致整个县城。终于，当局派地方军队包围了法商学院，三青团在内部配合，威胁学生复课。　　<br />
<br />
正在城固县城城里的妈妈和同学们听说地方军队包围了校区的法商学院，当即从城里出发去救援法商学院被困的同学。第一批赶到的卢贵英等同学冲了进去，妈妈和后来赶到的同学被阻在外面，他们便围在外面喊口号，鼓动里面的同学冲出包围。　　<br />
<br />
终于，卢贵英带着一批同学冲了出来，他在第一排中间，和两旁的同学紧紧地臂膀挽着臂膀大步走出来。他们口中大声呼喊着口号，脸上纵横流淌着眼泪，头上嘶鸣呼啸着枪弹。这场面时时令妈妈热血沸腾，那激动人心的画面永远烙在妈妈的脑海中。　　<br />
<br />
从法商学院冲出来的同学都集中住进了文理学院的大礼堂内。一位学生自治会负责人召集同学们在大礼堂开会，他宣布：为避免重大牺牲，主席团解散，学运结束，请同学们分散躲藏。那些一直暗中指挥学运的地下共产党负责人早已都消失藏匿了起来。　　<br />
<br />
同学们闻此均痛哭流涕，坚持不散。学运从一九四六年一月初闹到五月，闹了四个月竟然如此收场，同学们都心有不甘。　　<br />
<br />
有些三青团同学向着学运，常给妈妈通风报信，这时又有人来告诉她说：“你和卢贵英已被列入逮捕黑名单，你们得赶快走，别从大门走，现在大门口附近都是眼线。”　　<br />
<br />
于是，妈妈和大卢跑到校区僻静之处，翻过学校围墙，一起躲入乡村。躲了几天后，妈妈回校探听消息，路上遇见法商学院的法文教授，他是天主教徒，他马上把妈妈带到一个天主教修女院内避难。这位教授对妈妈心存好感，表示愿意帮助妈妈出国留学。可是妈想到那么多同学下落不明，自己不忍心远走高飞，便婉言谢绝了他的好意。在妈妈躲藏在修女院的一个星期中，数位西北大学的同学被捕了。西北大学的同学们再次举行了大游行，他们抬着妈妈的巨幅画像慢慢地行进在城固县城中，无声地声援被捕和失踪的同学。　　<br />
<br />
风声平静后，妈妈从修女院出来回到了学校，与在校的同学商量如何去汉中看望被捕的同学。同学石敬仁的父亲在汉中法院当首席检查官，妈妈他们通过石敬仁父亲的帮助去监狱探望同学，并设法营救他们出狱。　　<br />
<br />
政府军队镇压西北大学学运的消息传到全国，各大报纸都相继报导，各地媒体都对政府发出抗议声讨。在舆论的压力下，陕西省高等法院汉中分院匆匆宣布：西北大学学运在押学生判刑一年，缓刑三年执行，准予分别取保释放。　　<br />
<br />
学运失败后，妈妈和三个男同学卢贵英、石敬仁等聚在一起共同商量今后的出路和前途。卢贵英提出马上办理留学手续，请神甫帮助写留学推荐信，他家有钱，他可以提供路费买船票去美国。妈妈坚决不同意，说：“现在国难当头，我们不能做逃兵跑到国外去，我们应该跟着共产党干革命，做一名解放全中国劳苦大众、实现共产主义的小卒。”　　<br />
<br />
结果，大家一致同意妈妈的意见，决定去共产党领区。他们四人于五月底先回 到北平，通过共产党城市工干部介绍，于七月份到达共产党领占的城市张家口。这时，妈妈改名为罗理，表明追求真理的决心，卢贵英改名为卢介夫。　　<br />
<br />
一到张家口，共产党组织就要求妈妈立即返回北平到中部城市工作部工作，后考虑到妈妈已在西北大学学运中出头露面、大名在外，还在通缉之中，遂又改变决定，分配妈妈和大卢一同到华北联大学习。当下的华北联大是在半年前抗战胜利后从河北阜平县迁移到张家口的。　　<br />
<br />
进入联大学习要填写一张表，其中有一栏是“已婚/未婚”。大卢找到妈妈商量：如果俩人填未婚，以后分配工作可能会把俩人分开，再难相聚。他问妈妈是否填已婚? ?妈妈欣然答应，于是俩人都填了已婚。联大校方见两人填的表后就说：“你们怎么不早说呵？”于是，校方把他们俩人安排在一间屋住下，两个年轻恋人就这么结婚了，没有婚礼和仪式。妈妈和卢介夫都被分在政治班学习，卢介夫还被选为学生会主席。　　<br />
<br />
妈妈和大卢进入华北联大刚一个月，就赶上国民政府准备攻打张家口，八路军只得于一九四六年八月撤出了张家口。尔后，国民政府于一九四六年十月十一日收回了这个日本投降后八路军接管的第一个城市。　　<br />
<br />
一九四六年八月，妈妈和大卢跟随撤离张家口的联大队伍长途行军一千多里， 翻越太行山，步行一个多月，一路风餐露宿，历尽艰险，最后来到河北省束鹿县（后改名辛集县）的农村安置下来。　　<br />
<br />
到河北束鹿后，妈妈和大卢这些年轻学生就感到当地的政治气氛异常紧张。这里的土地改革运动刚开始，运动来得凶猛，不少地方喊出“贫雇农打天下，坐天下” 的口号。土改很快便发展成为残酷斗争、严厉打击的整肃运动使许多地区对知识份子干部吊绑毒打。到一九四七年时，这个最初为土地改革的运动便演变为共产党整肃内部的名为“三查、三整”的政治运动：对非军事人员要“三查”，即查阶级、查思想、查作风；对作战部队要“三整”，即整顿组织、整顿思想、整顿作风。一时间，共产党统治区人心惶惶，气氛肃杀，腥风血雨。　　<br />
<br />
这突如其来的运动使妈妈和大卢这些刚到共产党领区几个月的年轻纯真的大学生惊恐万分，不知所措。妈妈和大卢的出身都不符合共产党的阶级标准，第一关“查阶级” 就过不了关，出身于大地主家庭的大卢就更遭了殃。一九四七年初，大卢就被中共党组织带走隔离审查了，妈妈对他的去向完全无从知晓。更糟糕的是，妈妈此时怀孕两个月了。到一九四七年五月时，妈妈也被列为重点审查对象了。妈妈的审查是在小组内、班内、同学之间进行的，而大卢的审查却不知在何处，妈妈已经几个月见不着他的面了。　　<br />
<br />
妈妈在回忆录中写道：“我的家庭情况实在乌七八糟，我厌恶这个家，才离家走出来。但是对于这个家的每一个成员的工作和他们的具体活动，复杂的社会关系等等，我从没关心过，因此一无所知或所知甚少。‘三查’时，我根本说不清楚家庭情况，这样一来，本来简单的事情变得复杂了，我被列为重点审查对象。于是个别谈话、小组帮助，从生活到政治，从社会关系到反动组织，越谈越乱越糊涂。写了很多材料也满足不了热心帮助我的同志们。他们把我看得太高了，认为一个出身于大城市复杂家庭的大小姐，一个大学学生运动的领袖，一个上了国民党黑名单而能逃避追捕的人，思想和经历怎么可能如此简单。他们怀疑我是敌人派到革命队伍中来的，还怀疑我隐瞒了不可告人的目的，追问我为什么到解放区来。其实，共产党干部中不少人有过这样的遭遇，在国区党占领区被认为是共产党，在解放前被认为是日特、国特，我也受到同样待遇。好心人看我怀着大肚子，思想沉重，痛苦不堪，瘦得满脸就剩两颗大眼睛，反复交待还是过不了关，于 是劝我说，算了吧，别这样死心眼了，他们要什么? 就写什么不就结了。我想，党教导我们对组织要 老实，为了自己过关说假话欺骗组织，岂不成了投机分子？我不能瞎说。”　　<br />
<br />
妈妈最终也没谈清楚她的复杂的家庭背景和投奔共产党的目的，联大校方却在审查妈妈三个月后不了了之地停止了对她的审查。审查一停止，妈妈才意识到自己即将临产了，需要马上准备一些食品和用品。在临生产几天前，妈妈独自拖着待产的身子给自己做了一些油炒面。三个月的审查使妈妈神经紧张、抑郁，又患上严重的营养不良。妈妈担心自己生孩子后没奶，于是她抓紧时间在附近的村子里找到一位刚生了孩子的张姓妇女，求她在自己生产后能临时来奶几天孩子。八月初，妈妈生下一个男孩。由于心情极度的悲戚沮丧，妈妈竟连孩子的出生日都没记下来。　　<br />
<br />
妈妈生产四天后，联大党组织给隔离审查了七个多月的大卢两天时间，准他回家看看刚生产的妻子和儿子。性格刚直的大卢抓住这两天两夜的时间彻底地了结了半年多来的屈辱，毅然地从难以承受的屈辱中解脱了。　　<br />
<br />
妈妈在回忆录中这样描述了大卢最后两天的日子：“第一天回来，他瞪着惊慌的眼睛站在门口，完全变了一个人，又黄又瘦，神情紧张，不成样子。我被他的样子吓坏了，怯怯地问他：‘你站在门口干什么？’他怯怯地问我： ‘我可以进来吗？” 我说：‘快进来呀！你怎么了，怕什么？’他走进来抱住我就哭了，说：‘我真想你啊！可见到你了，你受苦了。’我劝他不要这样，坚强些。接着，我说：‘我很好。我们好好谈谈，怎么回事’他说：‘没法谈啊，我把我所知道的都实实在在地说了，没有人相信，而他们所提的问题我一点也不知道。他们坚持说我是国民党派到解放区来的特务，我告诉他们西北大学学运是受地下共产党组织领导开展的，是反对国民党的。他们说那是国民党搞的假地下党。我有一个褓母曾做过妇女会的工作，他们就说我们家是日本的奸细。’ 我说：‘这没什么，只不过是组织向我们了解一些情况，了解清楚就没事了。我同样受到过诬陷和委屈，现在不是没事了吗？’ 他说：‘你和我不一样，你们家穷，是两个不同的阶级。’我劝他不要胡思乱想，出身由不得自己，现在我们投奔共产党参加革命，已经背叛了过去的资产阶级、地主阶级，是无产阶级的革命干部。”　<br />
<br />
“我给他冲了一碗炒面，他急切地大口吞食，我看出他有一段时间没有正常地吃饭了。吃完后他就要走，我说：‘你不能走，我需要你照顾。’他说：‘不成啊，要开会的。’我硬拉着他，说：‘开什么会？！你需要休息，我去找领导谈，不用管什么开会，你就呆在这里。’我给他吃了两片镇静药，按着他睡下。当时我生小孩才五天，身体非常虚弱，一看天晚了，就没有去找领导谈。大卢睡得很好，第二天起来，他说：‘我给你买点吃的去。’接近中午，他端来一盆鸡汤，我问他哪来的钱买鸡。他说：‘我用戒指换的，让老乡做的。我说：‘你发疯了，金戒指多值钱呀，才换一只鸡。他说：‘戒指不能吃，你还是快趁热把这只鸡吃了吧。我真的吃了半只鸡，喝了一碗汤。他眼泪汪汪地看着我吃，双手不停地发抖。我问：‘你是不是病了？’他说：‘没病，我心疼，对不起你。’我笑着说：‘咱们同国民党斗争时，在枪林弹雨中都没怕，现在只是审查有什么怕的，我们实话实说，泰然处之。’他说：‘不是怕，而是士可杀不可辱。真是在劫难逃啊！’”　　<br />
<br />
下午，大卢又出去了，走时他没有告诉妈妈他要去干什么。大卢刚离开，就来了两位联大的同学来看妈妈，妈妈不认识他们，他们问妈妈身体好吗，需要什么，他们可以负责去向领导反映妈妈的请求。妈妈对他们的问候表示感谢，趁机向他们谈了她对大卢的担心，并提出自己的请求。妈妈说：“大卢现在的精神很紧张，态度举止不太正常，你们注意些，同他好好谈谈。他是个书呆子，没有经历过这样的政治运动，恐怕一时想不开，会出问题。另外请你们代我向领导给大卢请几天假，一方面他可以休息一下，同时我也需要他来照顾我几天。”他们说：“没问题，你好好休息吧。”　　<br />
<br />
刚说到这里，大卢回来了，这两个人一见到大卢就马上一改刚才的态度，一人一边就把大卢架了起来，厉声地说：“走！走！”连拉带扯地把大卢拉走了。晚饭后，天都黑了，妈妈已经躺下休息了，大卢又回来了。妈妈问他去哪儿了，他说：“到办公室和同志们谈话，谈得很好。他让妈妈放心躺着休息。看他精神萎靡、疲惫不堪的样子，妈妈让他早点休息，什么也不要想了。他说：挺好的！我什么也不想了。”他走到妈妈的床前，拉住妈妈的手，眼泪汪汪地看着躺在床上的妈妈，在床前跪了下来，口里不停地说：“罗理原谅我吧！罗理原谅我吧！妈妈劝他快起来， 要坚强，说：“你在西北大学那种英雄不可一世的样子哪里去了？没有过不去的坎！等我身体好些咱们还有很多事情要商量呢，你这样子，我也不能休息。” 可是，大卢坚持跪在妈妈的床前不起来。　　<br />
<br />
刚生下孩子才五天的妈妈早已身心俱疲、头晕目眩，很快就困乏得迷迷糊糊地睡了过去。在忧恐不安的时醒时睡，妈妈隐约地感到，大卢一直静静地跪在那里攥着她的手，深情地看着她。直到黎明时，妈妈才沉沉地睡去。这时，跪了一夜的大卢慢慢地站起身，静静地离开了屋子。他纵然对妻子、孩子百般留恋，却万念俱灰、万般决绝地走了。天上正下着小雨，他走到井边，先喝农药，再跳井，毅然决然地结束了自己年轻的生命。　卢家的独苗子，卢家唯一的希望，西北大学的高材生就这样悲惨地死了，死时年仅二十六岁。　　<br />
<br />
妈妈回忆录中写道： “我一觉醒来，只见婴儿，却不见大卢，马上意识到不好，穿上衣服就往外跑，这时外面下着小雨，我不顾一切地跑到大卢老同学的住室，喊道：‘ 快起来！大卢不见了，快去找！’ 大卢这位老同学姓杨，与大卢中学时就是同学，他比我和大卢早一年投奔共产党，此时在联大工作。他看见我惊慌失措的样子吃惊地站起来说：‘怎么回事？你怎么跑出来了？’他把我按在他的床上，给我盖好被子，说：‘你不要动，我去找大卢。’我躺在那里，脑海里像过电影一样浮现着大卢一幕幕的影像。这位老同学过一会儿回来后对我说：‘大卢找到了，他有点病送医院了，你放心好好休息，有我们照顾他。’我回到住室仍是放心不下，村里的张大嫂来给孩子喂奶，我见到她就哭了。她说，你不要哭，刚生小孩没几天会哭坏身体，为了孩子想开点。过了几天，院长何干之找我谈话，他告诉我，卢介夫投井自杀了。他还说，大卢这个举动是不对的，令人惋惜等等。我略微想过之后立即向何干之表示，大卢此举是消极背叛革命，我不会因此动摇和消极，我要坚定地跟着党走，为革命事业奋斗到底。”<br />
<br />
尽管妈妈当即坚定地对党组织表明了自己的态度和决心，她还是一夜之间成了反革命家属，周围的大多数人都不敢接近她了。这个巨大的打击使妈妈的奶水一下子彻底地没有了。义务给孩子喂奶的张大嫂来了，妈妈告诉她，丈夫自杀了，现在自己已经是反革命家属了。妈妈问张大嫂怕不怕受牵连，张大嫂一边给孩子喂奶一边说： “什么是反革命家属我不懂，我只想救孩子一条小命。” 张大嫂走后不久，孩子由于没吃饱又哭起来，哭累了就睡着了，过一会醒了还是饿得哭。张大嫂有自己的孩子要哺乳，不能再奶妈妈的孩子，她便给妈妈介绍了另一位奶妈。于是，妈妈托着产后虚弱的身子，抱着孩子走到十几里外的村子找这位奶妈。　　<br />
<br />
路上，妈妈遇见出村去为食堂买东西的联大的马车，赶车的食堂管理员认识妈妈，可是这次他见到抱着孩子的妈妈却没有像往常一样停下车带妈妈一程，而是视而不见，赶着马车从妈妈身边过去了。妈妈流着眼泪抱着孩子走了十几里路，找到了张大嫂介绍的奶妈家。　　<br />
<br />
这个奶妈刚死了孩子，她一见妈妈就高兴地接过孩子，撩起衣服给孩子喂起奶来。妈妈看到她奶水充足，就放心把孩子留下了。可是，妈妈还没走出村子，就有人走过来告诉妈妈，这个女人是城里人，有梅毒，孩子一生下死就了，现在她是到村里来养病的。一听此言，妈妈吓得立刻跑回去找个藉口把孩子又抱了出来。这时，天已经黑了，妈妈摸索着沿着一条羊肠小路走到一个池塘边，哭着坐了下来，心情极度抑郁的妈妈望着塘水，失去了活下去的勇气。妈妈流着眼泪久久地看着正睡着的没了父亲的孩子，孩子那安宁的样子使妈妈实在忍不下心离他而去，看着看着，伟大的母爱渐渐地复苏了，妈妈咬牙站起来，抑制住极度的悲伤，抱着孩子硬撑着走了十几里路走回了家。回到家，妈妈就一下扑倒在床上，虚弱得一动不动了。　　<br />
<br />
第二天，华北联大系主任找妈妈谈话，他说：“现在是战争年代，领导的孩子都送给人，你现在的情况抚养孩子太困难了。”妈妈何尝不知道一人抚养孩子的艰难苦楚，可是越是苦，她越感到孩子是她的感情支撑，看见孩子如同见到大卢，感情上精神上都得到安慰，所以她咬牙坚持不把孩子送人。当天妈妈又抱着孩子去别的村子找奶妈，还是无功而返。过了两天，张大嫂才给妈妈找到了一位耳聋的奶妈。妈妈终于把孩子送了出去。妈妈得到休息后，体力慢慢恢复了。　　<br />
<br />
大卢自杀的同一天，另外两个华北联大的教员也因为受不了严酷审查自杀了，其中一位女教员自杀未遂。却不知从哪儿搞到一颗八路军的土制手榴弹，她把手榴弹在胸放前，平躺在地上，拉开了引爆线。没想到，这颗手榴弹是次品，它没有把这位女教师炸死，却把她的下巴炸掉了。　　<br />
<br />
大卢自杀一个多星期后，华北联大的党组织决定把妈妈分配到联大附近的辛集中学教书，就算她从华北联大毕业了。　　<br />
<br />
河北辛集中学是共产党在抗日战争胜利前夜创建的一所中学。原名为“晋察冀边区第六中学”，妈妈在时易名为“冀中区第二中学”，办学宗旨是“一切服务革命战争需要，一切为了革命战争胜利”。　　<br />
<br />
联大离辛集中学将近十多里路，妈妈带着行李坐着马车走了一个小时就到了辛集中学。妈妈刚到辛集中学没几天就赶上中共河北省教育厅召开教育大会，辛集中学校长便让妈妈去参加会议，大会的第一天妈妈就巧遇爸爸，于是就发生了上述的爸爸和妈妈的爱情故事。　　<br />
<br />
一九四七年的八月到九月这一个月是妈妈的劫难之月，也是妈妈的幸运之月，这一个月中妈妈经历了人生巨大的迭宕沉浮。　　<br />
<br />
当时，虽然妈妈被允许在共产党的学校做教师，也允许她参加大会，但是共产党的档案制度却使妈妈背上反革命家属的标签不得翻身。不管她如何极力向党表忠心，不管她工作多么出色，她也很难摘掉这个标签。更遭的是，她的孩子也将成为反革命子女。爸爸的出现，使妈妈摆脱屈辱的境地成为可能。　　<br />
<br />
尽管妈妈一直对共产党非常愚忠，但是妈妈老年后的一些表现却让我们子女大跌眼镜。那是一九八四年初的一天下午，一位不速之客突然来拜访妈妈，他竟是四十年前在西北大学与妈妈单线联系的中共地下党员杨丹。他听说我爸爸出事的消息，找上门来突然拜访。我的爸爸在年前（一九八三年）十月以新闻出版代表团团长的身份赴美，在访美期间滞留不归，成为中共史上第一位叛离中共、留在西方的中共高级干部。　　<br />
<br />
爸爸叛离的消息马上被上报给邓小平，报告说，爸爸是共产党员的儿子，本人属抗战牌的共产党老干部，曾是中共建政初期的外交官。邓小平得知这些后，大怒，说:“刘珙是罗孚式的人物！”罗孚曾是老党员、老干部，后被中共定为“英美帝国主义间谍”，囚禁北京十年。根据邓小平的态度，中共中央立即向全国下达文件，通告把爸爸开除党籍，开除公职的处分。一时间，海外报纸热点报导。　　<br />
<br />
杨丹得知消息便前来向妈妈表示同情，并特地藉此机会向妈妈吐露埋在心中近四十年的爱慕之情，他问妈妈现在他是否还有希望得到她的感情。妈妈听了他的话反倒气不打一处来，好像自己不是共产党员似的，大声斥责起共产党和杨丹来。　妈妈指着杨丹说：“你们共产党都是些什么东西？就会在底下煽风点火，危险一来就闻风而逃。当初学运失败前，你们共产党组织早就知道国民党政府要镇压学生了，可是你们一边在背后继续煽，一边内部决定，只通知产党员在镇压前撤退，不通知学生，让学生做挡箭牌。此后还是三青团的同学给我报信让我快逃，你当初在哪儿！学运失败后，那么多学生被捕，你们共产党根本不管，都是我们学生自己四处奔波找关系救人，你当初在哪儿！你们共产党人面兽心，就会利用人，利用完就整、就杀，大卢就是被共产党整死的，他死得好冤哪！我丈夫刘珙对我恩重如山， 是个大好人，共产党要是有他那么有人性，也就不会整死、杀死那么多人啦！”　　<br />
<br />
妈妈情绪激动，口若悬河，杨丹被骂得口干舌燥。他劝妈妈：“你能不能给我沏杯热茶？”妈妈到厨房给他泡了一杯茶叶水──用的是冰冷的自来水。　　<br />
<br />
妈妈在回忆录中写道：“刘珙不仅是我的丈夫，还是我的恩人，他一生都实践着当初结婚时对我的诺言。”　　<br />
<br />
大卢的死不但是妈妈终生的痛楚，也是爸爸终生的痛楚。爸爸老年时常常陷入那些痛苦的回忆中。有一次他从回忆中恢复过来像是自问自答似地对我说：“共产党整人往死里整，让人感到无路可逃，文化大革命时整人就更是让人无路可逃。可是一九四七年时中国还不是共产党的天下，在共产党领区挨整的人也感到无路可逃，他们宁愿自杀也不逃，他们完全可以逃啊！”当时我感到爸爸几十年后还在为大卢 和其他挨整自杀的人伤心。　　<br />
<br />
我在美国与爸爸生活了八年。可是直到他去世十年后，我才知道爸爸一直带在身边的一个如书包大小的小藤箱的秘密。原来那是大卢送给妈妈的唯一一件礼物，这只小藤箱成为爸爸和妈妈共同的念物，里面装着爸爸和妈妈年轻时的来往书信。一九八三年离开中国时，爸爸把这只小藤箱和里面的书信一并带到了美国，始终把它保留在身边一直到死。</div>

							
							
							
							
