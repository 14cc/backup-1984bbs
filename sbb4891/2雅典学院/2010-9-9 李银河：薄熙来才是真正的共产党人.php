													<h2>李银河：薄熙来才是真正的共产党人</h2>
						
						
																											<div id="postmessage_821483" class="t_msgfont"><a href="http://blog.sina.com.cn/s/blog_473d53360100l8c1.html" target="_blank">http://blog.sina.com.cn/s/blog_473d53360100l8c1.html</a><br />
<br />
在重庆成功扫黑之后，薄熙来又开始发动反腐，采取奖励实名举报的办法，相信利用利益推动的办法，重庆的吏治将会更加清明起来。<br />
在我看来，薄熙来的作为说明他是一个真正理想主义的共产党人。当年，我们的父辈参加革命都是怀抱着一种改造中国的理想，一个黑社会欺压百姓、官员贪污腐化的社会绝对不是他们愿意看到的。<br />
记得在80年代，人们还都不习惯把各级领导叫作“官”，人们心中的干部应当是人民的勤务员，刘少奇和掏粪工时传祥握手并说“我们只是分工不同”的事情深入人心。干部根本不是旧社会的“官”。曾几何时，所有人都自然到不能再自然地把当干部叫作当官。资本主义社会的官都能公布收入，共产党的官却做不到；资本主义的香港都能做到基本上廉政，共产党的中国却做不到，这怎么说得过去？好像有一股难以控制的力量（人的贪欲）把中国向“无官不贪”“三年穷知县十万雪花银”的深渊拖去。<br />
如果有一批薄熙来这样的人，共产党就还有希望。否则中国只能被一种可怕的无比强大的惯性拖回到几千年吏治的老路上去。所有的规则都是那么熟悉，所有的逻辑都是那么难以破解。如果不是历史正在和已经变成现实，吴思的“潜规则”也不会那么畅销，那么引起共鸣。<br />
尽管有一大批薄熙来这样的抱着最初共产党人理想主义的人在力挽狂澜，恐怕要真正走出历史循环，还要靠制度的改革。近来，胡锦涛和温家宝都在讲政治体制改革，这就是要从制度上解决问题的信号。在我看来，中国政治体制改革的最终目标就是开党禁，搞大选。这是全世界政治体制改良的大势所趋。尽管这种制度在中国历史上没有过成功的先例，但是台湾政治改革的成功已经提供了一个小小的范例，台湾与我们同文同种，它至少证明，中国人也能适应这种制度。<br />
这一改革也许会对共产党不利，但是并不违反理想主义的共产党人的初衷。因为我们相信，有薄熙来这样的共产党人，共产党被选为执政党的几率还是很大的；因为我们坚信，共产党的执政绝对不是历史上农民起义改朝换代的翻版，我们共产党人的理想不是做强加于人的执政党，而是做由人民选择的执政党。共产党从用枪杆子取得执政资格到用选举取得执政资格的过渡阶段正在到来，它的启动信号已经由胡锦涛和温家宝发出了。<br />
