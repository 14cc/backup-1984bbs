<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;江湖雀语：今日新闻说贬值<br />
<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; ・首届中国经济年会专家称：房价8千到3万，不是房价涨了，是人民币贬值了74%。<br />
&nbsp; &nbsp;&nbsp; &nbsp; ・在去年的经济危机中，因为政府极其关注民生，我国老百姓的收入没有下降。<br />
&nbsp; &nbsp;&nbsp; &nbsp; ・我国居民收入不高，其中劳动收入的比例持续下降。我国储蓄长期负利率很不公平。<br />
&nbsp; &nbsp;&nbsp; &nbsp; ・有专家提出：房地产业应当为居住服务，不能成为经济发展的支柱。<br />
&nbsp; &nbsp;&nbsp; &nbsp; ・专家称：二次房改不可避免，香港50%以上的人住在政府的保障房中，新加坡为80%。<br />
&nbsp; &nbsp;&nbsp; &nbsp; ・杭州去年卖地得1,200亿。地方政府收入有50%为卖地所得者，而土地是有限的。<br />
&nbsp; &nbsp;&nbsp; &nbsp; ・中国超越美国为世界第一汽车产销国，但没有核心技术和研发能力，亦无自主名牌。<br />
&nbsp; &nbsp;&nbsp; &nbsp; ・美国专家质疑中国经济增长难以持久。因其特色为：以投资为主，高耗能、高污染。<br />
&nbsp; &nbsp;&nbsp; &nbsp; ・根据中国设定的标准打分，中国与印度经济情况最好，日本居中，美国、欧盟最差。<br />
&nbsp; &nbsp;&nbsp; &nbsp; ・“日航”破产原因：一、曾经为国营，民营化后依然受到国家保护；二、福利太高。<br />
&nbsp; &nbsp;&nbsp; &nbsp; ・郑州城雕“金色的梦”原定贴金，因为网民反对改变方案。此为政府虚心接受意见。<br />
&nbsp; &nbsp;&nbsp; &nbsp; ・央视二台的“第一时间”栏目“新闻海底捞，麻辣一点评”已改为“读报一点评”。<br />
&nbsp; &nbsp;&nbsp; &nbsp; ・海地维和烈士灵柩回京，被交通管制的汽车鸣笛，此为中国人走向世界公民的表现。<br />
&nbsp; &nbsp;&nbsp; &nbsp; ・手机扫黄中的“黄色”标准包括“含蓄的”和“能引起联想的”，引发争议。<br />
&nbsp; &nbsp;&nbsp; &nbsp; ・过年不要做金钱攀比，以免得“年关综合症”。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 美国人说人民币估值过低，应该升值；而我们的房地产商说，人民币已经贬值近四分之三了。这让我们老百姓信谁的好呢？或者美国人是根据中国政府宣布的数字，以及卖到美国去的便宜货得出的结论，而房地产商则是不想让我们知道，他们已经剥了我们的皮？好在我们自己的收入有没有贬值，我们自己明白，在政府的“极其民生关注”下，工资收入按比例持续下降，储蓄的保命钱长期贬值，买房子的钱更是高速贬值……其实质无疑是：我们老百姓在贬值。<br />
&nbsp; &nbsp;&nbsp; &nbsp; 不明白的是：为什么明明知道房地产业不能作为经济支柱，当年还要搞“房改”？现在又要再来一次，好像香港、新加坡……是昨天才盖政府保障房的。正是因为当年的“房改”，地方政府才变成了与房地产商勾结，盘剥老百姓的“地霸”，现在要与虎谋皮，容易吗？<br />
&nbsp; &nbsp;&nbsp; &nbsp; 不用房地产拉动经济，用汽车业又如何？我们不是已经世界第一了吗？问题是：真正的第一恐怕只是在加工上。要是人家抽去核心技术、设计专利、以及品牌，我们大概连一辆汽车也造不出来，就是勉强造出来，恐怕也没有几个人肯买。所以，面对别人的批评，还是不要用自己的标准去打压，这种农民的小市民化表现，只能使我们大中国自贬其值。<br />
&nbsp; &nbsp;&nbsp; &nbsp; 想想“日航”的破产：国营、国家保护和高福利，这些，不就是我们的支柱企业眼下的通病吗？也就是说，如果真要讲投资效益、能耗比，严格的治理污染，我们的支柱企业就有可能步“日航”的后尘而贬值。这，正是我们的政府想给自己贴金，老百姓不肯的原因。<br />
&nbsp; &nbsp;&nbsp; &nbsp; 真不懂，我们的政府官员学士的学士、硕士的硕士，博士也成行成市了，为什么还不明白不让批评不等于没有缺点呢？“麻辣一点评”也容不下，难道不是其智商的贬值吗？<br />
&nbsp; &nbsp;&nbsp; &nbsp; 比如，就算我们当被交通管制的汽车鸣笛不是表示不满，难道我们就不明白，向殉职的警察致哀，不能代替包庇打死老百姓的警察吗？更糟糕的是手机扫黄，其标准居然包括“含蓄的”和“能引起联想的”。那么，“打飞机”的军事防空是不能提了；“雄起”的足球口号也不能喊了；“……被逼骑马，穿过浓云密雨，累得气喘吁吁，哼哼唧唧，浑身打颤……”就只好被枪毙了。<br />
&nbsp; &nbsp;&nbsp; &nbsp; 然而，这难道不是“年关综合症”式的精神疾病：“扫黄综合症”吗？<br />
&nbsp; &nbsp;&nbsp; &nbsp; 可见，我们这个社会要是再不改变运行规则，贬值的就不会仅仅是人民币了。<br />
&nbsp; &nbsp;&nbsp; &nbsp; 可悲！<br />
