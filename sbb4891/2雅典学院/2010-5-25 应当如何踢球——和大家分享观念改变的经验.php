													<h2>应当如何踢球――和大家分享观念改变的经验</h2>
						
						
																											<div id="postmessage_637208" class="t_msgfont">（自己都想不到，居然写了这么多。从来没有在任何一个论坛发过这么长的帖子，看的朋友辛苦了。）<br />
<br />
首先在这里声明，这不是一个炫耀帖。相信坛子里很多朋友足球都踢得很好，我在这里写这篇文章就是想和大家分享我的足球成长经历，希望大家能从中有所收获。供大家参考，欢迎讨论。而我全文的主旨是：足球的真谛在于团队，在于传递，在于进攻和防守中的配合。先此言明，请大家了解。<br />
<br />
掐指一算，发现自己已经踢了将近20年的足球了。大学毕业的时候就想写一篇回顾多年来足球经历的文章。那时候的想象是应该把一段段成长故事融进足球生活里。昨天出差，卧铺上一夜失眠，回忆近期和大家一起踢球，就想自己能有什么贡献给我们这个1984的球队。干脆写一篇文章吧，把自己的感悟和大家分享。至于另一种叙事体的文字，估计在这次之后，就自然的拖延了……<br />
<br />
我10岁开始踢球，小学四年级的暑假。那时踢的，是老爸从学校里带回来的一个排球。排球体积和足球略同，但是却很轻，如今想来，可能还算是比较适合小孩子启蒙用。后来看到巴西的孩子都是拿网球练，终于明白人家的球性为什么那么好。从此时开始到小学毕业，基本上都是一群孩子疯跑，追来追去。是纯粹的“球到哪、人到哪”，而不是球跟着人走。不过小时候这几年基本上培养了初步的球性，可以操作简单的停球、传球、带球还有过人，当然这都是面对同龄人。在那个一岁一层身体素质差异的年代，差一岁的年龄，就基本上能完全体现在身体能力上。<br />
<br />
上了初中，成了班上的足球队长，一直打中场。攻的时候得上，防的时候要回。我的体能不好，比赛都是拿命踢。初中学习紧、成绩差，好多时间都花在足球上。我妈管得严，喋喋不休让我少踢。有一次班里争年级冠军，因为成绩不好，班主任拖了我十分钟才让我上场。后来有一次，老妈去开家长会，我们踢球等家长。我妈回来说，她散会之后透过教学楼玻璃窗看我在球场上拖着虚脱的身体进攻防守，终于明白足球于我的意义。所以以后除了督促我学习，再没有拼命管过我踢球。<br />
<br />
那个时候踢球，虽然也明白中国队踢得臭就是因为没有传递，但是一到自己上场，看着场边挤得满满的女同学，真的难以抑制自己带球过人的冲动。那时候的我们，就是拿球的时候自己拼命狂奔，没球的时候拼命叫队友传球给自己。我还多了一项任务，就是在防守时不停的喊队友对位回防。后来上大三，开始频繁的打五人制小场，又开始喊队友积极的围抢。<br />
<br />
初中最难忘的一次经历，就是去一个大学踢野球，跟几个大学生一起踢。每次退防的时候，一个大哥都会不断的喊“跟住人！别出脚！”自此明白，能让别人传，能让别人射，就是不能让人家过。在所有防守失误中，被人过而丢位，代价最大。这样的后果，不是造成威胁性传球和进球，就是被迫犯规，送给对方机会。<br />
<br />
说到这里禁不住插几句骄傲的话。我从小踢野球长大，从来没受过任何正规的培训。小学没有，中学六年学校没有校队，大学本科时学校有特招生而且自己水平差。后来终于有机会进校队了，校队老师看完我几场比赛，说我只要考上研究生，就能进校队。结果两次落榜，终成遗憾……毕业以后就更没有了。不过好在野球踢的多，我同学说我人到哪，球鞋带到哪，在好几个城市不同的球场跟不同的人踢。交过脚的多了，觉得自己在从没有接受过正规训练的人里，算是水平比较高的。从开始踢球到近几年，在技术方面，每隔几个月，就觉得自己有所进步。我身体素质尚可，但比很多人差，很多花式动作到现在都不会做，只能一步一步随着球感慢慢进步。而且后来发现，炫目的技术其实重要性并不是很大。而我足球观念上的进步，则不是逐渐提高，倒像是通过几次重要的经历有所领悟。<br />
<br />
该说高中了。高中的时候分到另一个班，气氛不好。由于和同学关系处的不够融洽，一直打拖后后腰或中后卫。那个时候练过一阵左脚，但是仍然延续着带球过人的憧憬。现在想起来，上高中以后我们的身体技术都好，但是两次因为点球惜败，从没得到过冠军。究其原因，就是大家总是独自向前，很少配合传递，导致不能在比赛时间内解决问题。那时候的传球，往往不是主动传出去，而是不得不传或者是失误造成传球。真是暴殄天物啊！对不起足球……<br />
<br />
上大学了，大一大二开始进系队、年级队、院队。大家还都是喜欢自己带而不是传给队友。但是现在不一样了，对手往往有经验，自己也开始懂事儿了。无论是打中场还是后场，无数次因为自己失误或者被对方断球导致丢球。在全队的进攻中，本来大家一致向前，结果因为自己的失误，全队都要折返跑帮你挽救局面。即使没有造成进球，你也得向大家道歉，怪我怪我，应该传出去的……这可是大场啊！一次折返多少体力啊！即使你的带球没有导致丢球，只是被破坏或者被扑出，其实本来也是有更好的选择，传出去有更好的空档和机会。这种情况会使队友形成巨大的心理挫折感。几次进攻之后，和你一起跑的人明显减少，全队的进攻效率大减。须知足球是一个集体项目，最大的快乐是全队所有队员的触球和传递。而一旦变成一个人的游戏，这个队伍就基本无路可走了。大学初期这两年大场踢的多，主要的总结就是两个：一个是带球的时候尽量动起来而不是原地做动作，否则很有可能被对方扑上来；另一个最重要，就是要传！这个阶段是观念上一个重要的成长期。后面我还会在观看比赛的感悟方面对此进行说明。<br />
<br />
下一个阶段是大三大四，主要的收获是懂得无球跑动的重要性（包括防守中的团队配合）。大三开始踢五人制，以大禁区为界。这个要感谢学校一个足球场的大禁区，基本算得上是当地最高水平的五人场。当地退下来的专业运动员、一些水平不错的老家伙还有很强的留学生，下午四、五点之后纷纷聚集，能凑四、五个队，一球一下。为了能多踢一会儿，那个激烈程度简直令人紧张，不过也让人进步很快。这种五人制必须有一个人守门，其他四个人大致两前两后。这种几乎人对人的攻防，第一是个人很难突破，第二是如果队友不接应，很难把球传出去。结果就是队中的每个人都要动起来，不断的寻找空档。第一个层次是要保证有两个接应点，再高一个层次，就是在第一下传球的时候，前两个人能够想到第三个人，也就是第二下传递。而如果一个人带球时其他人不跑动，这个人基本就废了；如果队友跑出空档你不传，以后别人不跑，你就独撑危局吧……与此相应的，是防守时前场的联合拼抢。当一个人对拿球人进行骚扰的时候，本方必须有至少一人进行配合拼抢，干扰其传球的接应。这样，就可以从源头扰乱对方的进攻，在实战中特别有效。上述接应性的攻防跑动不仅锻炼身体，而且特别动脑子。你要不停的想应该往哪里跑来接球，下一步又要怎样传出去，还要怎样配合队友的拼抢。有关的观看比赛经验，我也会在后文详述。<br />
<br />
这一段的磨练让我有很大的提升。此后正好第二年考研，不仅时间充裕而且是自由身，再加上一些朋友都成了队长，所以在一个学期里帮两个院打校内联赛，帮一个系打院内联赛（第二年考研再败，与此大有关系）。在这三个队里，我都打后腰。帮助一个弱院打进决赛阶段，另一个院也打得不错，至于那个系，帮他们从弱旅打进决赛。助过攻、进过球，还带过一个帽子（不是绿的）。虽然我对整个球队的贡献不应夸张，但就是在校内联赛中，被校队老师相中。因为他发现，我比他校队中的主力，打得好。从给全队拖后腿的人打到队里的核心，我觉得技术的进步相当有限，最重要的是思想的转变。真是应了那句话，思想有多远，我们就能走多远。懂得把球踢连贯，不禁反思，曾经为何踢得如此蒙昧？确实，后来我们懂得应当怎么踢，就把当初的自己，称做蒙昧……<br />
<br />
关于跑动，在这里插一个小曲。上大学后有一次回家和小学同学踢球，一个哥们儿是秦皇岛足校毕业的。后来我问他，你为什么没踢中超？他说鲁能来要人，看完他的比赛跟他说，你要是有1米7，我们就把你带走了。他踢后腰，技术意识都好，因为身高退役，可惜了。那次和他踢球，我就自以为技术好，也想拿球。他一边带球一边冲我喊，说你想要球你就跑位啊！你在我身边我给你有什么用啊！我知道他专业，回来就反复想他说的话。是啊！要是能够通过无球跑位过人，何必拼了命的带着球过呢？是不？<br />
<br />
来北京之后也有过一次踢球经历比较难忘。有一次去清华踢野球，我和几个学生对几个老家伙，踢小场。我们年轻气盛，对方人老珠黄。但是没天理了，我们拿到球三下五除二就传丢了、带丢了。他们永远是集团推进，每人间隔三、五米，几乎不停球，持续的传递。我们完全都被人家的节奏和传递带傻了。然后再眼睁睁的看着人家射门，根本防不过来。后来看巴萨，越发体会这种传递和跑动的威力。<br />
<br />
最后和大家分享看比赛的一些感悟吧。我喜欢踢，不太喜欢看。如果看，只看自己特别喜欢的人和队。小的时候看优美的动作和射门，关心谁输谁赢。后来逐渐懂了怎么从看球去学东西。我看的不多，这里仅说我看过的。<br />
<br />
先说看团队。团队防守有几个经典队伍：荷兰、切沃和巴萨。荷兰的特点就是全攻全守，防守的时候真的是所有的人都在跑。当防守队员跑得比进攻队员还积极的时候，进攻中的传球就成了一项难题。荷兰当初的二六二阵型，防守的两个人是在两个边路，这曾经让我纳闷，防守的时候中路谁来负责呢？而当我看到罗纳尔多带球狂奔，是中场的进攻组织者戴维斯回来充当中后卫一路追一路铲，才明白这种防守也是需要特殊人才的。相比之下，巴萨的防守体系就更加成熟了。不要以为巴萨只是进攻华丽，仔细看看他的防守，你就会明白他的整体防守能力绝对可以在包括各个国家队的球队中排进前五。每当丢球或者防守，巴萨都是全队在动。不仅针对无球队员对位盯防，而且往往集中三个人对带球者进行围抢。巴萨不断的创造奇迹，不仅在于进攻华丽而有效，更有坚定的防守作为后盾。最后不得不说的是意甲的切沃。当初切沃从乙级联赛辅一升级，就创造了意甲的升班马神话。这不仅在于全队齐心，而且有一套特别独特的防守方法。那个时候喜欢鲁伊科斯塔，看米兰对阵切沃，只要米兰在反击时有一个队员带球进攻（在切沃半场），切沃的全体防守队员就一致扑向他。这种防守的结局是带球队员就算再神奇也不可能同时穿过三、四个人，而一旦把球向前传，队友就几乎肯定进入越位陷阱。结果只能是横传或回传，从而使反击速度大减。这种防守当初令意甲众多球队头疼，一时难觅良方。<br />
<br />
说到团队进攻，我知识有限，仅说我所感觉的几个原则。以2006年世界杯流行的四二三一阵型为例。进攻过程中，当一个边前卫突破或传中，另一个边前卫必须在后点接应。在他身后扯边的，应当是本侧的边后卫。而当这个边后卫上前助攻，另一侧的边后卫必须在防守位置上，和两名中后卫形成三人防守。也就是说，当一侧的边前卫带球向前，应当是对侧的边前、边后同时上前；而如果是本侧边后卫已上，则对侧边后卫必须留守。而对于双后腰来说，两边的中圈和禁区弧顶是攻防中的关键区域，而且都必须一动一静。攻时一个穿插、一个分球，守时一个跟防对方的核心、一个站位补位。<br />
<br />
而谈到个人，我是从上大学后开始学会看整体中的个人。因为懂得读书要精读，那么比赛应该也可以精看。而精看的一个方面，就是看你想关注的单个球员。我喜欢后腰，所以看过维埃拉和皮尔洛，正好是一守一攻。看维埃拉是看进攻的时候他干什么，防守时他怎么跑、盯谁，什么时候应该补位，补哪些关键点。而看皮尔洛，是看他怎么回来接球，怎么分球，怎么跑，如何控制节奏。大家可以根据自己的爱好，选择自己喜欢的位置和球星来重点学习。我看皮尔洛，有一个方面的原因，就是自己体能差，而发现皮尔洛近乎匀速的跑动特别能够节省体力，值得一学。在这里说一句废话，就是球场上也是术业有专攻。每个位置的很多技术动作完全不同，就像有一次范志毅被推到后腰位置上打了一场比赛。赛后表现欠佳的范志毅说，我多年没有打中场，背身拿球的感觉和中后卫正对来球完全不同，我甚至不知道应当如何转身、摆脱防守、并把球分出去。<br />
<br />
最后也是最重点的，是我想请大家注意，好的球队和球员都是触球最简练、传球最多、带球相对较少的。关于传球，大家可以看看巴萨，巴萨在比赛中的传递甚至是像在遛猴儿。基本是能传就传、有人就传。像很多人评论的，巴萨的传递是令对手窒息的传递。他在传递的过程中，不断的强迫防守方转移注意力，不断的变向跑动，累到你精疲力竭，失去跑的动力。不停的传递，会让传接越来越熟练、越来越快，空档也就层出不穷。而关于触球，不只是巴萨，大家可以留意任何一支强队，每个球员的单次触球基本不会超过三下。即使是我很喜欢的盘球大师齐达内，也往往在触球三下之内把球解决出去，尽管看上去很华丽。而即使是在前锋位置上表现出色的如梅西、卡卡、C罗、米利托等，他们成功的助攻和进球往往是在触球较少的情况下。而大量盘带的效果其实并不尽如人意，对此大家可以做相关统计。而其实有些看似长途的奔袭，真正的触球往往很少。这和我们日常足球中，特别是业余比赛中泛滥的盘带形成鲜明的对比。<br />
<br />
写到这里，才发现想的时候虽然腹稿也不少，但真没想到，居然洋洋洒洒的写了这么多。作简要的总结，就是对于足球这样一个集体项目来说，团队配合才是王道。要简洁处理球、减少触球、增加传递；攻时多空跑；守时多围抢。至于上场之前的基本功？我觉得目标就是：停球能够将肩部以下一米之内的来球停在身体周围一米之内；传球则能够传到你希望传到的点的一米之内。具备以上的基本功，再加上成熟的意识，基本上可以应付我们一般的业余比赛了。而一个球队只要思路清晰的坚持训练，总有熟能生巧的一天。<br />
<br />
我的经验尚属业余，仅供大家参考，欢迎交流讨论。曾经还梦想有一天到发达国家拿个足球理论的学位，不知道命中有没有这个幸运。本文构思成于南下的列车上，谨向该车致敬。而你，如果不厌其烦的读完了我的文字，请接受我真诚的谢意。</div>

							
							
							
							
