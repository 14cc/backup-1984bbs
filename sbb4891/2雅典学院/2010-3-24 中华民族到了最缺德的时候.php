													<h2>中华民族到了最缺德的时候</h2>
						
						
																											<div id="postmessage_530197" class="t_msgfont">中国曾经是一个十分推崇道德的国家，“礼义廉耻”“孔孟之道”曾经主宰着中国人几千年的精神世界。<br />
<br />
　　“ 西方价值观”自然有其长处；但是，这几十年来，中国人从西方学到的，只是是弱肉强食的法则，只是利益最大化的价值取向，而西方社会的的“诚信”“ 民主”和“法制”却被有意识的“扬弃”了，中国人的传统道德观也被无情的丢掉了。该“接轨”的接轨了，该“特色”的特色了；开放的中国人向极端自私的世界观急速蜕变，在经历30年的经济高速发展后，大部分人的道德水准急剧下降，人的劣根性得到了最彻底的发挥。<br />
　　<br />
　　古老的中国曾经有过“礼义廉耻 ”的教育，“孔孟之道”的熏陶，有过“斗私批修”的灵魂洗礼，有过“雷锋精神”的伟大情操；我们既有过信仰宗教的历史，也有过信仰共产主义的年代，中国什么都学过，但是，在举国发展经济“致富最光荣”的浪潮中，“一切向钱看”让所有中国人都希望自己能够成为“一部分先富起来的人”，唯有个人的利益才是最实际的，而道德、良心、信誉等等，统统被抛弃了。<br />
　　<br />
　　于是乎，官场热衷于大搞形象工程、政绩工程、GDP崇拜----只要是能来钱的项目，无论是深度环境污染项目，还是明显的短视短期行为，不管三七二十一照上不误。任江河湖海污染、任青山秀水变色。至于只图眼前利益、一任政绩而大卖农田林地、大赚其钱的，更比比皆是。而所有这些，都会在冠冕堂皇的“公”字旗号下进行，有的还为此类行为罩上一层光辉的油彩和美丽的面纱。<br />
　　<br />
　　于是乎，一些官员私欲膨胀，奉行“人不为己，天诛地灭”的信条，拜倒在孔方兄脚下，钻进钱眼里不能自拔。有些人以权谋私，权钱交易，贪赃枉法，贪污腐败；习惯于暗箱操作，醉心于权力寻租；一人得道，鸡犬升天；大权在握，公帑国库悉数成一己私产；置房产，买别墅，养小蜜，包二奶，不亦乐乎；挥霍浪费公款已是小菜一碟，中饱私囊实成家常便饭……这些人，名为官，实为蠹，早已沦为不折不扣的“钱奴”。<br />
　　<br />
　　于是乎，恶商、奸商唯利是图、见利忘义、巧取豪夺、坑人害民：卖木耳的用福尔马林浸泡，卖火腿的用敌敌畏熏制，卖食盐的用工业盐充数，卖黄鳝的给黄鳝喂避孕药，三鹿奶粉更披着国家科技进步奖外衣坑害后代……。他们弄虚作假，作奸犯科，无所不用其极。黑心的房地产商上通贪官，下结地痞，哄抬房价，欺行霸市，牟取暴利，坑害百姓。至于黑煤矿之类的窑矿主，更是视人命如草芥，视黑煤如黑金，只要有钱可赚、有利可图，哪管他人死活？<br />
　　<br />
　　于是乎，升斗小民见利忘义，地痞流氓肆意妄为，黑社会横行霸道。你要问个路吗，对不起，拿点“好处费”来！人与人，变成了冷冰冰的铜板关系！甚至在目睹有人落水之时，竟然毫不掩饰地大叫：“拿钱来就救！”还有诸如“笑贫不笑娼(黑、匪，诸如此类)”的论调也大行于世，一些人竟不以为耻，反以为荣。人性在金钱面前黯然失色，良知在金钱面前霍然泯灭。甚至对那些伸出援手、助人为乐的善行，不以为然、冷嘲热讽，做阱陷害，敲诈勒索；以至操弄司法，为虎作伥。人人以金钱为衡量一事一物的取舍标准，渐渐地将这个社会推向邪路。<br />
　　<br />
　　古老的中国尽管没有真正意义上的宗教信仰，却有着强大的道德规范的约束，然而，当一个民族的道德观被彻底粉碎的时候，法律能够约束到人们心灵的每个角落吗？更何况在一个法律并不健全，又常常被强权破坏的国家里，那些层出不穷的“缺德”事件，仅仅暴露的是法制、管理上的问题吗？中国人常常说，“头顶三尺有神灵 ”，可那不过是一些受害者对不法分子的诅咒罢了，做缺德事情的人从来就是将自己的利益放在至高无上的位置上的。<br />
　　<br />
　　回顾一下中国几十年来发生的那些缺德事，从工业酒精兑酒，到注水猪、瘦肉精，从苏丹红到阴沟油，从大头奶粉到“三聚氰氨”，从南京徐老太到荆州捞尸人......中国人的良心已经堕落到连婴儿、死人都不放过了，而这些还只不过是冰山的一角。<br />
　　<br />
　　于是乎，在960万平方公里的大舞台上，时时上演着这样的悲喜剧：<br />
　　<br />
　　------看到有人摔倒可千万别去扶,这一扶搞不好就是几万块钱的医药费；<br />
　　<br />
　　------看到马路上有人偷东西可千万别吱声,你这一喊多半被小偷围殴一通；<br />
　　<br />
　　------想要跳楼、跳河放心的跳吧，不会有人拦你也不会有人救你；<br />
　　<br />
　　------如果遇到入室盗窃的,最好假装睡着不要高声呼救，以免遭到人身侵害；<br />
　　<br />
　　------如果你与之搏斗,打不过你倒霉,打的过也不能打死打残,否则还是你倒霉；<br />
　　<br />
　　事不关己，高高挂起；好事无人敢做，坏事无人敢管！这就是和谐国的生存之道。<br />
　　<br />
　　这还是哪个有5000年文明史的古老中国吗？<br />
　　<br />
　　曾经被祖先高高地供在庙堂之上的“道德”“良心”“正义”，今人还有吗？<br />
　　<br />
　　两千五百年前，管子就把“礼义廉耻”作为一个国家立国的根基，“国有四维，一曰礼、二曰义、三曰廉、四曰耻。一维绝则倾，二维绝则危，三维绝则覆，四维绝则灭。”一个丧失“礼义廉耻”的国家是不可能维持长久存在。<br />
