													<h2>魏京生爆六四最新内幕：王维林生死和屠杀人数</h2>
						
						
																											<div id="postmessage_399540" class="t_msgfont">2008-06-06 <br />
<br />
魏京生在英国出席《勿忘六四：历史，现实与未来》讨论会。<br />
 <br />
被称为“中国的曼德拉”并获多次诺贝尔和平奖提名的魏京生先生出席了6月6日在伦敦的西敏斯大学和6月7日在剑桥大学召开的《勿忘六四：历史，现实与未来》讨论会，与中西人士一起缅怀“六四”，回顾历史，讨论中国目前的热点话题，分析中国民主运动未来的走向。以下报导是本报记者根据两个讨论会的记录综合整理做出的。<br />
<br />
回顾“六四” <br />
<br />
参加讨论会的人们有的是青年学生，对于“六四”到底发生了什么事情一无所知。还有人并不相信中共的军队杀了人。魏先生说，“到底‘六四’有多少人死了？1993年北京卫生医疗机构统计是死了6700多学生，不包括中央国家机关的，也不包括军队在广场上直接处理的。后来（军队）调来了直升飞机，开始是说运弹药，实际是运尸体。有空军人士说，直升机运了4天尸体，北京实际上死了有上万人。” 魏先生表示很多的官兵们当时并不想杀自己的同胞。38军的徐军长公开抗命，结果被抓起来了。但是杀人的命令是从上面下达的。他特别提到了他所知道的一名勇士只身挡坦克的情况。他说，“其实木樨地的王维林，（人们）根本不知道他叫什么名字。这事是我的老同学处理的。当时，38军从复兴门这条路开过来，最前面的那个师的师长就是我的老同学。他就坐在首车里，所谓的王维林在那儿挡的是他的车。国外看到的镜头和国内看到的不是一个镜头。国内中央电视台放的是复兴门的镜头，国外是北京饭店的。两个完全一样的人，做同样的事，镜头略微有点差别。当时，驾驶员问他（我的同学）怎么办，能不能绕过去。所以，你能看到（录像上）坦克绕了一下。可小伙子横着过来了。驾驶员又问，师长怎么办？ 没过10分钟，天上的直升飞机来电话了，问：你们怎么回事？他们说，前面有老百姓挡着。直升飞机上的人说，‘什么老百姓，是暴徒！’他说，‘我看了确实是老百姓，不能向前走。没有什么暴徒。又过了5分种，直升飞机可能请示了上级。飞机上的人说，‘首长命令了，你们马上进到军事博物管院里调整，让113师上。’113师的师长刚刚升了官。我的哥儿们（记者注：哥儿们是北京方言，即朋友的意思）就告诉侦察连的把这小子抓起来。至少先别让他死了，这人的生命太危险了。可是不知道怎么搞的一个排的侦察兵居然没有看住他，让他给溜出来了。他又去挡的时候，113师的坦克连停都不停，哗一下就过去了，直接就把他捻成肉饼了。而且那时侯，人身上什么都不带，你也不知道这人是谁，根本不知道这人是谁。” 魏京生谈到“六四”大屠杀的背景时说，“邓小平在“六四”中杀人，为了树立他的权威。赵紫阳希望通过这场运动使邓小平下台。但是邓小平代表的是一些利益集团，他们是不允许你们学生这么干的，所以邓小平最后决定杀人。当时在广场上都是支持学生的。学生们提出了反腐败。当学生又回到天安门时，我所在的监狱里的警察就说可能中国共产党要开枪。所有人都希望解决这个问题。如果不杀人，共产党要对整个社会让步，解决了对谁都好，对中共、对老百姓都好。当时在广场除了中共中央委员会的牌子没有，连中共中央办公厅的牌子都有，国务院的大旗都有人看到过。干部都是趋向学生这边的。很多人都是反对镇压的。譬如，洪学智（军委的执行副主席，有实权，可以指挥军队）秦基伟（国防部长）都是直接抵制的，民心所向吗。 他还评论说，‘六四’仿佛煽了所有人一个耳光，大家的希望破灭了，对共产党的希望破灭了。对整个社会的希望破灭了，对学生民主运动的失望，对所有的东西都很冷淡。对共产党的失望。没有对它的信仰，打破了几十年来共产党对中国人教育，人们对共产党的迷信。破除了这个迷信。”<br />
<br />
70年代的西单民主墙 <br />
<br />
应与会者大要求，向大家介绍了70年代末的西单民主墙，他所倡导的“第五个现代化”，以及为此他在中共的监狱里无辜地被关了18年的一些细节。他说，“当时民主墙的大部人是为中共荐言的。在民主墙那里卖杂志很快就被人抢购一空，10元一份。民主墙的气氛很热烈。当时很多人不同意我们的观点。他们说，你们说共产党要倒台？因为从小受的教育，觉得共产党是爱人民的。当时我们说的“第五个现代化”很多人觉得很好，也有很多人不能接受，人们觉得邓小平改革了，干吗还要政治民主，还要政治现代化呀。有人也提出这个看法：能让我们过好日子就行了，以后再说吧。79年大家情绪很热烈。当时邓小平玩了一个手腕：老百姓需要经济变好，先把经济搞好了，人民就会支持他。他先打了一仗，打越南，转移视线，建立自己的威信。越战中中国的死亡人数很大。而且打完仗以后，边界线还往后退几十米。那个战争是有背后的政治目的的，普通的老百姓很难知道的。<br />
<br />
今年的四川地震<br />
<br />
魏先生在谈到前不久的四川大地震时说，“1976年，唐山大地震时，部队是在几个小时之内就赶到了现场。现在比以前更现代化了，可是这次军队却以各种借口，如天气不好等等，迟迟不动。如果军人们都是幼儿园的小朋友，可以原谅。但是，你们是空降兵。在任何情况下都应该打仗。怎么能说天气不好就不打仗、不跳伞了呢？！我是当过兵的人。最不能让老百姓容忍的是，一些政府大楼完好无损，而许多的孩子却被砸死。什么原因？腐败造成的，建筑质量太差。” 有大陆留学生质问魏先生是不是知道地震的真实情况，地震发生后13分钟后新浪网上的新闻是什么。他回答说，“我们大家看到的信息可能是不一样的。不能说，我看的东西你没看到，我看的就是不真实的。据我知道，中共政府自己都承认军队是在第三天才到现场的。另外，你说温家宝在地震一个小时之后就起飞到灾区。这件事恰恰是网上大家讨论最热的话题。很多专家揭发，他们在地震前就把预报上报了，但是中共当局没有通知老百姓。很多人在讨论中质问温家宝是不是事先知道，要不然他怎么能一个小时之后就上了飞机。实际上按正常运作，地震发生后，一般是层层上报的，然后由整个政府来做决定。而且，地震后灾区的通讯不是完全畅通。当然，还有报导说，防化兵在地震前两天就进入了灾区。 救灾过程中，温家宝一直在电视屏幕上。作为总理，温家宝当时应该是指挥救灾而不是在电视机前流泪，实际上这是引起灾区人民愤怒的一个重大问题。我提醒这位朋友，除了看中共的官方网站之外，也应该看看不亲共的网站。它们放了许多在国内网站上贴上去几分钟就被删去的消息。许多老百姓不相信新浪网就像不相信人民日报一样。当然，许多网站上的东西需要证实。我在18岁以前，对于官方的消息我是先相信，找到证据后不相信；18岁以后我变成了先是不相信，找到证据后才敢相信。这是我们受骗多年积累的经验。” <br />
<br />
如何在中国真正地反腐败？<br />
<br />
针对这个问题，魏先生的回答是，“有人觉得，‘六四’的反腐与老百姓无关。现在看来非常有关。前几天在看新闻时，我看到很多老百姓出资盖的学校楼几乎没有倒的，政府出钱盖的却都倒了。现在没有人说那些死亡的孩子与腐败无关。所以很多孩子的家长说，“两分天灾，八分人祸。” 反腐，中共自己尝试了很多次，但是都不成功。所以，大家现在提出只有一个办法可以成功地造成民主和反腐败，那就是共产党下台。现在大家讨论的问题基本上都是怎么能让共产党下台。说在共产党继续执政的前提下我们怎么反腐败，其实已经没有人对这个问题感兴趣了。 ‘六四’之前，很多的中国老百姓还对中共存有希望，希望它能从内部进行改革。但是‘六四’这个镇压把大家扇醒了。93年我被放出来时发现，很多的人都不是在想中共是不是应该下台，而是在想怎么能让它下台。在我第二次被捕后（记者注：魏京生在1993年被第一次释放的六个月之后,第二次逮捕），连监狱的警察都在问我怎么能让中共下台。” <br />
<br />
是否应该和中共一起渐进地搞民主？ <br />
<br />
在场的一个大陆留学生建议我们是不是应该和中共一起渐进地搞民主。在回答这个问题时，魏先生说，“78年西单民主墙时，大多数人都跟你的这种想法一样，像我这种想法的人是极少数。共产党在这30年以来用各种各样的事实教育了大家，现在大多数人都有我这种看法了，只有少数人有你那样的看法。过去大家认为走上民主的路，它可能会是一条很平坦的路，共产党会和我们一起走。在这样的情况下我们渐进地、一步一步地去建立民主，因为民主不可能一下子就建立好。但30年来甚至60年来共产党不断地提醒我们：‘这条路不是平的。我是一个障碍，我挡在这里不许你们搞民主。’所以不是咱们渐渐地就能搞民主。 有人提出我们悄悄地、搞曲里拐弯地搞民主，把共产党骗了搞民主。有个监狱的老警察对我说，‘老魏，你妄想钻我们的空子。我告诉你，我们的无产阶级专政是非常严密的。你只有两个选择：你要民主那你就到监狱里来陪着我。你要是放弃了民主，你可以到外面去和大家一样自由地生活。我们的无产阶级专政是既严密又坚定的，你不可能动摇我们。所以你只有两种选择：你不要民主，否则你就得把我们推翻。’这是共产党说的，不是我说的。在89年‘六四’的时候，我们从电视上看到天安门广场上的学生们的活动，我说，‘唉，这些年轻人真傻。很多事情他们不懂。’那个老警察又说了一句很经典的话，‘那是因为他们没有尝够我们无产阶级专政的铁拳。’” <br />
<br />
<br />
<br />
<br />
<br />
<br />
<br />
魏京生和“六四”惨案幸存者、当年学生运动的组织者之一的绍江先生。（摄影：小龙）<br />
<br />
<br />
<br />
民族主义 <br />
<br />
魏先生认为，“89年以后，中共知道，再像‘六四’那样来对待民主是不行了的，它就做了调整，用民族主义来对付民主。而民族主义，如果把它推到极端的话，就会变成暴力。比如，纳粹希特勒就是靠民族主义起来的，他们开始是搞排外、排除异己。近代的很多独裁政权就是用这个方法来加强权力的。现代中国面临着很危险的倾向就是，极端的民族主义。它已经表现出来类似纳粹的倾向了：强烈的排外、排除异己。这样发展下去的话，会很危险。不仅我们很担心，中共的内部有很多人也在担心。” 什么是真正的爱国？ 讨论到这个问题时，他说，“我记得，89年4月份《纽约时报》有篇文章。当时，中国的形势就很紧张了，所以大家都很关心。记者讲那个地方（天安门）没有什么危险。那些学生最关心的就是三件事：第一是找女朋友，第二是考托福，第三是打牌。镇压发生之后，大游行发生之后，他的同事取笑他说，‘唉，你怎么回事？报的不对吗。’他说，‘这也不奇怪，每个人可以同时拥有各种各样的选择。’他的一个同事告诉我这件事的时候，认为他是在诡辩，可是我觉得他不是诡辩，他无意间说出了一个重要的真理：不是说你选择了考托福，你也选择了找女朋友，你就不可以关心民主了，你可以几样事情同时都关心。 那些年轻人，现在有很多80后，90后的朋友，可能觉得我们是爱国主义者，他们是搞民主的。其实错了，实际上大家都是爱国主义者。很多西方的记者批评我说，你这个家伙太爱国主义了。你不太符合西方的意识形态。我说，一个人，对国家不是爱得很深的话，你肯定不会为了他做牺牲的。所以那些为国家做牺牲的、争取民主自由的人，其实都是最爱国的人。国家有很多的缺陷，或者都病入膏肓了，这个时候你要不去为他治病，你怎么能说你是爱国呢？假设说有一个老头，挺有钱的，然后他病了。他儿子不是去给他治病，而是尽量掩盖他的病情，那么这时我们会做什么判断呢？我们可以说这个儿子在谋害这个老头。如果我们发现自己的国家有病，我们不去治它，而是去掩盖它，我们是在害这个国家，他根本就不是在爱国。” <br />
<br />
中国人民准备好了搞民主吗？<br />
<br />
“我觉得提这个问题，其实是个很实际的问题。中国社会对民主这个概念是不是做好准备？按照中国共产党40年代的说法，中国早已经做好准备了。当然，那个时候它还没有上台。共产党上台以后，它就开始说，中国的老百姓还没有做好准备，民主还得慢慢来。它召集了一大帮的博士在社科院里研究研究，就发明了一种理论。这种理论是说，中国人的文化呢，或者说中国人的这个种就是不行。所以说，中国人按照亚洲的这种文化，你们西方的民主到了中国就不可能。 我说，中国老百姓对民主这个概念其实早就做好准备了。要不然不会有几百万人、上千万人在街道上来要求民主。你们现在在西方可能也看到了，这个民主和中国老百姓在中国想像的民主不完全一样。在世界上，也有一些国家可能民主来得太快了，老百姓可能也不适应，也不懂。所以有人担心这个民主会不会被搞得乌七八糟的，这是经常发生的事情。但是，等老百姓都懂得民主再去实行民主的话，这恐怕也不太可能。比如过去你们在中国没有看到西方民主的一些内容、特点，我们应该向老百姓多做宣传，这样使老百姓在事先多了解一些民主的内容。那么中国民主成功的可能性就会提高一点。一个社会要真正学会民主的规则，一定是要在这个社会有了民主之后。”<br />
<br />
通过经济、文化、科技等方面的改革能不能和平地推动中国的政治改革？ <br />
<br />
“有人说，我们不敢得罪共产党，我们不去管政治方面，我们去搞经济文化、科学技术方面。但是几十年下来一次一次的失败。很多的中国知识份子都已经总结出了一个结论：这个专治的政治不改变的话，什么问题，连环境保护问题也搞不好。89年以后，腐败的问题大家都很关心。共产党中有很多人也在考虑，不改变一党专政能不能解决腐败问题。他们号召学者来进行讨论，让知识份子们在中共的官方报纸《人民日报》上公开讨论。我当时每天都在看报纸上的讨论。那场讨论一直到大家达到了一个共识，就是：绝对的权力导致绝对的腐败。我记得那是1996年秋天的时候。得出这个结论的时候，所有的讨论就都停止了。因为所有的学者们都知道，谁要再往下讨论，谁就会到监狱里去讨论。”<br />
<br />
如何推动中国的民主？<br />
<br />
“我记得一个笑话，在山西的农村，他们那个地方的人从来没有吃过鱼。知青（记者注：文化革命时，毛泽东搞了一个‘上山下乡’运动：把城里的知识青年送到农村去劳动，接受‘贫下中农的再教育’。知青是对这些知识青年的简称。）去了以后教他们吃鱼，先是详细地告诉他们怎么样吃不会被鱼刺卡住。可是，等他们吃鱼的时候，还是几乎每家有人被鱼刺卡住。但是在他们被鱼刺卡了几次以后，他们就知道怎么吃鱼了。也就是说，只有去做才能知道。 你们很多人对那些民主先贤的著作看了之后，看得滚瓜烂熟。但是你真正到了英国，到了美国，看到那个真实的民主的时候，你才能知道这个真实的民主是什么意思，是怎么回事，跟你在书上看到的是不一样的。 <br />
现在咱们就讲具体怎么去推动中国的民主呢，我想可能有很多种方法，各种各样的工作都会起作用。你看到的民主国家，他这个民主制度为什么比较好，或者他有什么毛病，把这个真实的情况告诉国内的朋友。如果有条件，写篇文章在报纸上报导。我想这样的工作都是一种很扎实的、经常需要做的而且很长期的工作。这也是民运每天都在做的工作。我们认为这个工作永远是非常重要的。这个工作呢，就是推动国际社会去关注中国的民主，关注中国的事务。国内有民主人士，或者说都不是民主人士，就是维权人士被捕的时候，我们尽量呼吁国际社会去营救他们，对他们形成一种保护。我想这也是对国内从事民主或维权人士的一种最实际的帮助。” <br />
<br />
FLG与器官摘除问题<br />
<br />
“FLG是一个信仰团体。他们所相信的内容我们可以不同意，但人家有权利相信，我们没有权利去指责人家。但是中共政府对法轮功信仰团体进行了残酷的镇压，那个镇压超过了对其它宗教的镇压残酷程度。我们知道中共对所有的宗教都进行镇压，但是对FLG进行了非常残酷的镇压，这方面的情况，超过了大家的容忍程度。 现在有许多人对FLG所报导的摘取器官的问题有怀疑。我想有怀疑这也是正常的。我虽然没有看到确切的证据证明FLG的学员被摘了器官，但是我在监狱里对于摘取器官问题进行过详细的调查。实际上从60年代起在中国的监狱里就开始摘取器官，用于各种用途。有些用来做医学试验，有些做移植方面的试验。我请一个和我在同一个牢房的被枪毙的犯人帮我证实一下，是不是在被枪毙前有拿着手术器的人在旁边等着。他帮我证实了这个事情。 魏先生告诉听众们这个死刑犯的故事：“这个小伙子是因为打架把别人打死了被关进死牢的。我听到他的故事以后，我觉得他的案情可能有点问题。我就帮他写了一份申诉书，而且我劝他请律师，因为律师给你辩护总是比你自己辩护好一点。但是，在他审判回来以后跟我说，‘老魏你可把我骗了，我白花75块钱请了一个律师，律师在帮那个检察官说话，不帮我说话。’他在等死的过程中，一方面他在听我讲故事，讲那个民主墙的故事，一方面总是在念叨，‘哎呀，我对不起我的母亲。’一会又念叨‘我对不起我女朋友’。他说，‘真后悔，我要早点认识你多好呀’。我说‘认识我有什么用啊？’他说，‘我要认识你跟你一块儿死的话，咱们也是条汉子呀。’后来，我就跟那个小伙子说，‘你现在想帮我做事也来得及，能不能帮我一个忙？’我突然想起我在监狱一直在调查死刑犯器官被摘取的问题。我说‘等你被枪毙的一天，到大厅里的时候，你能不能帮我证明一下确实有拿手术器械穿白大褂的人？’我们当时就约定了一个暗号，一个死刑犯当时不可能喊出来的一句话。‘如果当时确实有医生在那等着的话，那你就喊那句话。如果没有，你就什么也不说了，像其它犯人一样。’结果证明确实有医生在那等着。 即使这样我们还是认为证据不够充分。因为几个看守警察都跟我关系很好，称兄道弟的，我经常跟他们聊天。有一次聊天的时候，我就问道摘取死刑犯器官的事。我说，‘你们这么做是不是太残忍了，他当时就跟我说，反正他们也是死了，不摘也是白不摘。摘了增加点利用率，给我们的中央首长移植器官，也许能救活中央首长的，那不是很有价值吗。’我说，‘你们这样做有没有经过本人或家属的同意？’他说，‘1980年开始实行的新的刑法和刑事诉讼法之后，有很多细则。其中一部份细则规定，死刑犯的器官要被摘除的话，要本人同意，还要经过家属签字。我问，‘那摘取器官实际上家属签字了吗？’他说，‘这个实际上太麻烦，所以个个监狱都不执行。如果他们不执行，上面有不追究的话，那么大家就成了惯例了，也就都不执行了。只要本人同意就行。我问他‘有没有不同意的？’他说，‘这个好办。拿着他的手给按个手印就行了。’” 去年中共的卫生部长也承认移植的器官大部份来自于犯人的尸体。法轮功反对摘取器官的活动产生了非常重要的作用：至少从客观上促使中共当局在最近两年做出有关决定，对于器官的移植、器官的买卖做出了详细的规定。何其它许多的民主运动一样，法轮功对促进中国的民主、法制、文明等方面做出了很大的贡献。所以我也一直在支持法轮功。当然，我不仅仅支持法轮功的活动，我对中国所有受破坏受迫害的宗教团体都是支持的。我们大家都是很好的朋友。 剑桥大学的讨论会爆满，座位不够，很多人只好坐在楼梯上。 魏先生圆满结束英国之行。他认为英国人民和在英的华人们热情、善良。有这么多的人关心“六四”和中国的民主运动，相信中国的民主运动将会得到进一步的推动。</div>

							
							
							
							
