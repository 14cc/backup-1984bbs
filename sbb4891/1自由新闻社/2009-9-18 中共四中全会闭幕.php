																											<div id="postmessage_246479" class="t_msgfont">今日闭幕的中共十七届四中全会，将通过《中共中央关于加强和改进新形势下党的建设若干重大问题的决定》，这是中共近年通过的第三份党建文件。官方舆论早前大肆吹捧，指文件在中共党内民主及反贪制度建设方面将有突破。但是，由于中共坚持一党专政的体制，拒绝落实新闻和言论自由，反贪已沦为中共权斗的工具，四中全会的新文件，恐怕只是吹大反贪的泡沫而已。早在2001年9月，十五届六中全会就通过《中共中央关于加强和改进党的作风建设的决定》，2004年9月，十六届四中全会又通过《中共中央关于加强党的执政能力建设的决定》。但是，中国官场的贪腐问题越演越烈的趋势并未因此得到阻遏。中共既有的反腐肃贪模式，是其自鸣得意的两手抓模式，即一手靠自建政前延续下来的整风运动，一手靠中纪委查案纠风。近年内地官员须人人过关的大型整风运动，包括1998年的「三讲」运动（讲学习、讲政治、讲正气），2006年的「保先」运动（保持党员先进性），去年至今的学习科学发展观运动。这些运动的成效如何？民间调侃，官员「三讲」是「中午讲义气（喝酒），下午讲手气（赌博），晚上讲力气（嫖妓）」，「保先」则不如「保鲜」。还有人总结出官员吃饭的科学发展：「常与领导吃饭，升官是迟早的事；常与大款吃饭，发财是迟早的事；常与情人吃饭，肾虚是迟早的事。」至于中纪委，虽然在查办高官大案方面屡有作为，包括当年的北京市委书记陈希同、上海市委书记陈良宇，以及近期的广东省政协主席陈绍基、深圳市长许宗衡、广东省纪委前书记王华元等，但是，这些所谓大案的权斗痕迹太过明显，人们首先看到的不是中央肃贪的决心，而是清算北京帮、上海帮、广东帮的行动。民众讥讽，官员的情妇才是反贪的主力军，因为近年被情妇举报而下台的官员越来越多，包括解放军海军副司令王守业、陕西省政协副主席庞家钰等。反贪必须以制度建设为根本，不能靠政治运动、不能靠查案办案，这是历史经验的总结，也是社会的期待。外界原本以为十七届四中全会建立反贪制度，以为会落实势在必行的官员财产申报制度。但是，最新的消息指，财产申报制度在配套措施、制度创立等方面还面临实施难点和阻力，因此，四中全会的决定不会提出财产申报的细则和实施时间。由此可见，中共在反贪问题上只是越吹越响，正所谓一鼓作气、再而衰、三而竭，中共通过的第三份党建文件，还能带给民众甚么希望呢？就算有了财产申报、公示规定，但没有独立的核查及责任追究体系，对官员又能有多大震慑作用？惟有建立政党制衡、法律约束的机制，摒弃一党专政、党大于法的陋规，才能奠定反贪的基础；惟有落实新闻自由和言论自由，发挥舆论监督、网络监督的功效，才能确保反贪不流于空谈。周一至周六刊出</div>

							
							
							
							
