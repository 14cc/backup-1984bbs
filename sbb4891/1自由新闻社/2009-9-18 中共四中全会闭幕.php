													<h2>四中全会今天落幕 高层议案成焦点</h2>
						
						
																											<div id="postmessage_246495" class="t_msgfont">中共最高决策机关，中共中央委员会的全体会议将在今天落幕。围绕第十七届四中全会的召开，大陆官方连日来抓紧“党建”这一官方主题造势。然而，外界真正关注的仍是直接影响中国未来走向的高层领导层人事议案。<br />
　　根据惯例，中共将在三年后进行最高权力的交接，现任总书记Hu Jintao2012年交棒给下一代最高领导人。继任者的第一号候选人在2007年的中共全国代表大会后已确立。现年56岁的Xi Jinping当时从上海调任中央，出任中央政治局常委，兼任中央书记处书记和中央党校校长，这三位一体的安排，代表Xi Jinping完成接班的第一步骤。<br />
<br />
还无法排除出现“意外”情况<br />
　　如果Xi Jinping在本周的四中全会中进一步出任中央军委第一副主席，则意味着他完全实现Hu Jintao曾走过的接班前“两部曲”（即先出任中央常委，后在四中全会上接任军委副主席），顺利迈向2012年接班。<br />
　　不过，直到本周前，Xi Jinping是否能接任军委副主席，似乎一直未算十拿九稳。近期有消息指，军方人士提出看法，可否考虑适当延长Xi Jinping考察时间，“以免使人觉得不够民主”，显示Xi Jinping出任军委副主席出现“意外”的情况还无法排除。<br />
　　香港《信报财经新闻》署名任慧文的专栏文章在四中全会前夕写道，军中有人提出，军委体制应适当改革，考察军委主席接班人的时间应有弹性，不一定非在四中全会决定不可。如果考察认为够条件接班，当然可以在四中决定；如果考察认为不够满意，应打破惯例，延长考察时间。<br />
　　该专栏引述北京权威人士称，Xi Jinping去年领导一个中央小组负责处理3月西藏骚乱，又担任奥运会领导小组组长负责奥运安保等工作，表现获得肯定。今年7月5日新疆暴乱时，Hu Jintao正出国外访，Xi Jinping主张成立中央领导小组并参与其中处理事件，也被盛赞为果断、增添他出任第一军委副主席的筹码。<br />
　　据透露，中共当局向全党全军中央委员以上干部宣讲了Xi Jinping的上述政绩，为Xi Jinping被选为军委第一副主席制造舆论，并且“得到了较好的反响”，但专栏也说，此事还要经过各大军区二上二下的讨论，“但愿不致于会有什么意外”。<br />
至今四中全会进行了三天，密不透风的会场没有新消息传出。北京的政情观察人士普遍认为，Xi Jinping确立接班地位的可能性很高。“意外”情况如出现，就充分显露党内派系博弈之激烈。Xi Jinping是前中国国务院副总理习仲勋之子，属于高干子弟。两年前，当共青团出身干部在政坛上力量如日中天时，非团系出身的他却异军突起成为接班候选人，但是强大的团系也意味着他需要倍加努力才能真正接班。事实上，香港媒体上周放出的信号，已经透露出他接班道路至今仍面对着考验。<br />
　　不过，中共如果着眼于大局，这时候确认下一代总书记接班人选，对于维持内部稳定与对外形象都有好处与必要。接班人选是世代权力交接的关键环节，人选如不能按常例明朗化，继续保持模糊或者表现出“可变动性”，显然在党内会加剧人事斗争的风险。反之，接班人选大局底定有助于安定各政坛派系，对外则提高外界对中共政治走势的可预知性，传递政局稳定的信号。在当前强调“稳定压倒一切”的环境下，确定接班人选的益处大于将课题延后。<br />
　　中共的历届四中全会往往也是人事布局、人事角力会议。除了中央军委副主席外，据传中央军委委员也会有所增补。几个可能晋升中央军委委员的人选包括北京军区司令员房峰辉、沈阳军区司令员张又侠、成都军区政委张海阳等。<br />
　　事实上，党内出现建议考虑延长Xi Jinping考察期“以免使让人觉得不够民主”的声音，表明中共离强人政治时代越来越远，不同背景、分属不同政治派别的高干们要求对党内重大事务有直接发言权、决策权的意愿强烈而且直接。<br />
　　综合上述考虑，四中全会在今天闭幕时是否会端出下一代最高领导人人选，将反映出党内各派系的力量是否维持相对平衡，以及有权参与重大人事决策的各高层人物是否有足够的危机感，毕竟中共仍然还面对全球经济危机对中国的挑战，维持社会人心的稳定也依然是个艰巨的任务，在这个敏感时刻，中共更有必要对外发出团结一致的明确政治信号。</div>

							
							
							
							
