													<h2>世袭政权将加剧合法性危机</h2>
						
						
																											<div id="postmessage_246480" class="t_msgfont">生果日报<br />
<br />
中共十七届四中全会在北京召开。会议的一大看点，即现任国家副主席Xi Jinping（图），能否顺势出任中央军委副主席，以完备其作为「储君」的所有身份。从过往的惯例来看，Hu Jintao是在十五届四中全会（一九九九年）上出任这一职务的，三年后胡即正式接掌党印。由于军权在中国政治中的特殊份量，Xi Jinping如获任军委副主席，将意味着其「接班人」地位被正式确定下来。Xi Jinping目前的地位，是经过中共党内各派力量角逐的合力结果，除非习犯下令舆论哗然的严重错误，或中共权力格局出现重大变故，否则颠覆既定的权力安排，将导致权力大厦的结构性震荡，而这是处于危局中的中共各派都不愿看到的。就习获任军委副主席一职而言，如果今年不成，至迟也不过明年的事。之所以还有「今年不成」之说，是因为Xi Jinping刚进政治局两年，资历尚浅。当年Hu Jintao也是在被立为接班人、并晋身政治局七年后，才获任这一职务的。<br />
<br />
Xi Jinping拥有显赫背景<br />
与Hu Jintao相比，Xi Jinping最突出的特点是拥有显赫的家庭背景──其父习仲勋为中共开国元勋，因此，习属于典型的「太子党」、「官二代」。在中共党天下的背景下，Xi Jinping成为今天的「储君」，与其家庭背景有决定性关系。考诸习的个人能力与性格，在其主要施政地福建、浙江两省，公众普遍反映其能力平平，守成有余而开拓不足，是典型的「听话型」的干部。在太子党之中，习或许能算上较为突出的一位，但在人才济济的省部级干部队伍中，实属乏善可陈的庸常之辈。显然，习能有今天，决非能力使然。这一点，从其上调中央前的工作轨迹也能反映出来。习在中共上海市委书记的任上只干了短短两个多月，就被转往北京任政治局常委，创下了历任上海市委书记任期之短之最。毫无疑问，习在上海滩扎了个猛子就走人，决非中央的本意，更非因其工作突出，而是其它因素使然。个中内情，虽然外人无法详解，但可以肯定的是，这段时间中共内部上演了一场激烈的龙虎斗，结果是习背后的太子党一派占了上风。习被推至前台。虽说中共实行的是一党专制，所谓的选举也不过是走过场，但过去诸任党魁的「大位」毕竟还都是靠个人奋斗获取的，至少是按照党内游戏规则「拼搏进取」得来的。虽然以这种方式获取对国家的统治，对于人民而言并不具有合法性，但在党内尚能成立。反观Xi Jinping的「大位」，很大程度上是「世袭」而来的，只不过这种「世袭」不像封建时代的子承父位那么简单、直接，而是更为隐微、曲折，其实质是中共官二代集体世袭「马上得天下」的「第一代领导集体」，Xi Jinping不过是其中的一个代表而已。上网稍加搜索便知，中共太子党如今把持了多少政治经济要位，上行下效，如今连基层官二代也开始世袭起来。前段时间，媒体曝光河南固始县公开选拔乡镇长，结果选出的十二人中，除了一人出身富商家庭外，其余都是官员子弟或亲戚。<br />
<br />
领导早没有个人权威<br />
今天的中共最高领导人早已没有了毛邓时代的个人权威。胡温就已被讥为「政令不出中南海」。但胡温毕竟都出身平民，能有今天，也都是靠自己奋斗争来的。而Xi Jinping的太子党背景、无甚建树的从政经历，上台后如何能让各级官员心服口服？以这样的资历，又如何能驾驭住日渐自重的各路诸侯？如果说现在的中共政权非由民选产生，已经面临合法性危机，并导致「群体性事件」此起彼伏的话，那么Xi Jinping时代的中国状况如何，不难想象。江南　大陆自由撰稿人</div>

							
							
							
							
