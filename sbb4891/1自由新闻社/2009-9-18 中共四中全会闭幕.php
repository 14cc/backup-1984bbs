中新网9月18日电 为期四天的中国共产党第十七届中央委员会第四次全体会议今日将闭幕。以研究加强和改进新形势下党建问题的此次会议，适逢中华人民共和国成立60周年，也是中国共产党在世界上最大的发展中国家执政60周年，因而备受各界关注。<br />
<br />
9月8日召开的中共中央政治局会议透露，中共十七届四中全会将审议《中共中央关于加强和改进新形势下党的建设若干重大问题的决定》稿。据了解，这次全会前，决定稿已在中共党内外一定范围征求意见。各地区各部门各有关方面和中共十七大代表对文件稿提出的意见和建议，在决定稿中得到充分吸收和反映。权威人士认为，集中中共全党和各方面智慧制定的这份文件，将成为加强和改进新形势下中共自身建设的指导性文件。<br />
<br />
在庆祝新中国成立60周年前夕召开的此次中共中央全会备受各界关注。专家分析指出，此次会议将认真总结中共执政以来加强自身建设的宝贵经验，深入分析中共自身建设面临的新情况新问题，进一步研究和部署以改革创新精神推进中共自身建设。<br />
<br />
中共中央主办的机关刊物《求是》最新一期刊出中共中央党校常务副校长李景田的文章说，在88年的奋斗历程中，党多次经历关系自身前途命运甚至生死存亡的历史关节点，经受住了多次重大挫折或重大成绩的严峻考验。回顾党应对困难和成绩的考验，特别是在主要关节点上经受考验的历史，深刻总结经验，努力把握党的领导和执政规律，对于巩固党的执政地位，实现党在新时期的历史使命，具有重大的理论价值和现实意义。<br />
<br />
此次会议亦引起外媒的高度关注，《日本新华侨报》日前刊文说，历史证明，中共是一个非常善于总结历史经验而又善于运用历史经验的政党。此次党以全会的名义庄重地梳理和提炼已取得的宝贵经验，同时也总结和汲取教训，在今后执政中更好地把握规律性、富于创造性，担当振兴国家和民族的历史责任。作为世界第一大执政党的中共“党建”运作之道与治国安邦之策，必将为世界所瞩目。<br />
<br />
