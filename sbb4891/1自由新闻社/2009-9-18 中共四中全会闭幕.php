													<h2>郑宇硕谈中共十七届四中全会：“党内民主”无法带来监督制衡</h2>
						
						
																											<div id="postmessage_246696" class="t_msgfont">中国 | 2009.09.17 <br />
 <br />
中共第十七届中央委员会第四次全体会议9月15日至18日在北京召开，官方报道的议题主要包括&quot;研究加强和改进新形势下党的建设问题&quot;、&quot;党内民主&quot;和&quot;反腐败&quot;。而中国问题观察家特别关注的是Xi Jinping能否成为中央军委副主席，从而为在十八大上成为Hu Jintao的接班人作好铺垫。四中全会能否就新疆局势放出新的政策信号，也是会议的一个悬念。香港城市大学政治学教授郑宇硕在接受德国之声采访时对上述问题提出了自己的看法。 <br />
<br />
德国之声：中国以外媒体对十七届四中全会的报道中，都非常关注Xi Jinping能否成为中央军委副主席。您怎样评价中共高层在权力交接上的这一安排方式？<br />
<br />
郑宇硕：中共领导层在没有民主选举的情况之下，希望通过种种方式，把领导人继承的过程，做得比较平稳，维持稳定性。他们期望政权和政策有平稳的持续性。<br />
<br />
德国之声：如果Xi Jinping像外界预测的那样在本次会议上成为中央军委副主席，可不可以说他将成为Hu Jintao的接班人？<br />
<br />
郑宇硕：应该是这样的。大家都知道，早在上一代，就有一个提法，有这样一个安排，让Xi Jinping在2012年中共的第十八次代表大会上，接替Hu Jintao出任中共总书记。过去一年里，Xi Jinping主要负责筹办北京奥运，甚至可以说他的成绩得到肯定。如果四中全会上他出任中央军委副主席，他在接替Hu Jintao的权力继承安排上就更加稳妥了。也可能有其他的竞争者，但是真正能够统领党的工作，Xi Jinping同时也是中央书记处的常务书记，负责党的日常的工作。如果再加上军委副主席的地位，他将是在所有的竞争者中地位最突出的，可以说是在同辈当中占第一把交椅了。<br />
<br />
德国之声：Xi Jinping是中共内部几大势力当中所谓的&quot;太子党&quot;一派。那么如果他接班成为总书记，是不是意味着Hu Jintao代表的团派在权力交接安排上向太子党势力让步了呢？<br />
<br />
郑宇硕：这个让步早在2007年中共的十七大上已经作出了。当时的确有这样的传言，说Hu Jintao是支持Li Keqiang做他的总书记的接班人，但曾庆红等太子党力量否定了他这样的安排，推出Xi Jinping作为总书记的接班人。很可能Li Keqiang只是国务院总理的接班人。如果说是让步，也是比较早的时候就作出的，不是今天才作出的。事实上大家也知道，所谓的太子党在中国的政治经济影响力是非常大。这也反映出，在专制的政权里，既得利益者都为他们的家族安排好了后路。不但是在政治方面的发展，也在经济方面的发展，在政治权力上建立了一个差不多是垄断的地位。在经济上有这么一个说法，说中国的百分之零点四的家庭，占有了国内生产总值的70％。这些极少数的家庭，大部分也就是所谓的太子党的家庭。<br />
<br />
德国之声：我们看到中国官方的报道说，十七届四中全会的一个主要议题是&quot;加强党内民主&quot;。您认为本次会议上会有一些具体的措施出台吗？譬如说在基层党委书记选举上实行无记名投票制。<br />
<br />
郑宇硕：应该是有的，但也应该不是什么新意。所谓的&quot;党内民主化&quot;，主要的意思是这样的：自天安门事件以后，中共也不提什么政治改革了。他们所提出的基本上都是行政方面的改革。现在提的&quot;党内民主&quot;基本就是一条，就是维持党对政治权力的垄断。在党内搞民主话的用意，就是想保证各阶层的党的领导干部更重视民意，重视社会上对他们的评价。但基本上权力还是集中在党的手上。那么是否有具体措施呢？实际上在过去三十年里一直都有提，可能遭遇到一些阻力，现在再重新提出来。根据国内媒体的报道，大概可能会有这么几个具体的措施出台：第一个措施就是在各个层次的党代会，搞一个常任制，就是同级的党委书记，向同一极的党代表大会提交一个报告。代表们要对报告投票，如果对报告的支持率低于百分之七八十的话，那就说明有问题，说明这位书记在党内的支持率不高。还有一个措施就是很可能在党的基层组织民主选举领导班子，实行无记名投票。 主要也是希望各个层次的干部比较尊重民意。<br />
<br />
德国之声：显然这里说的党内民主和西方式的权力制衡式的民主有着本质不同。但是如果党内民主如果经过长时间演化，会不会形成一种&quot;准民主&quot;，即党内的确出现了能起到制衡力量的民主格局？<br />
<br />
郑宇硕：谈不上制衡。譬如现在贪污腐败的情况非常严重，先不说推动民主和法治，就是利用媒体监督和各级人大会议制度去问责这些官员这两件事中共都不愿意做。中共不希望出现任何党以外的权力，所以就没有制衡。就是谈了十多年的财产公开制度都没有落实，我个人对出现党内制衡力量一点都不乐观。其实腐败问题的提出也与当前的形势有关。中共领导层还是非常清醒的，他们知道经济发展放缓，失业问题严重，贫富悬殊，社会矛盾和社会不满高涨，他们必须要针对这些不满，在打击贪污和整顿干部方面多做功夫。<br />
<br />
德国之声：您认为四中全会会在新疆问题上有所讨论，反省一下中共的民族政策问题？<br />
<br />
郑宇硕：我猜测新疆问题不是这次全会的重要议题。面对少数民族问题，党的领导层不是愿意把这个问题拿出来比较公开地谈论。我猜中共领导层还是以不变应万变，即一方面加强对西藏和新疆的经济扶助，争取当地民众的好感，另一方面是尽快地恢复安定的局面。<br />
<br />
采访：潇阳<br />
责编：叶宣</div>

							
							
							
							
