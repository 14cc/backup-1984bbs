小纠纷变大冲突 民族问题症结 <br />
<br />
【明报专讯】宁夏回族自治区党委书记陈建国日前作出批示，要求当地公安全力解决银川市一间回族人经营的公司遭内部汉人员工联合其它汉人诈骗而损失45万元（人民币，下同）一案。但是，当地公安却不予理睬，未采取行动，该公司声称将组织公司内回族员工并联合当地其它族人赴银川市公安局等多个部门游行抗议。<br />
<br />
银川回族公司遭汉人诈骗<br />
<br />
银川保绿特生物技术公司由回族人金素文经营。06年9月，公司内部汉人职员勾结其它汉人侵占财产，造成45万元财产损失，令该公司难以为继。事件发生后，金素文曾经多次打110电话报警，但当地汉族公安将双方当事人带至警局做了一番调查取证后，竟下发「不予立案通知书」，再无下文。<br />
<br />
此后，金素文寻求法律途径以求得解决。09年11月，灵武县法院判决公司职员杨某须偿还12万元，但在法院执行过程中，杨已下落不明，执行被迫暂时终止，其它30多万损失也无法追究。<br />
<br />
金素文将问题的根源归咎于当地汉族公安的「不予立案」，并多次找银川市公安局要求讨说法。而银川市公安局副局长朱荣则认为，「不予立案」符合法律程序，无任何疑义，「此事不构成公安刑事立案标准，不需要任何解释」。<br />
<br />
警拒立案被指「民族歧视」<br />
<br />
此事虽获自治区党委书记陈建国批示，但公安答复却一直未能让金素文满意。金素文坚称，杨某等人与公安内部汉族警员存在联系，「整个事情过程，都是因为民族歧视造成的」。看来，即使在新疆、西藏这些敏感地区以外，民族矛盾也不时若隐若现，是内地发展的隐忧，如何做到避免民族个人之间的简单纠纷「擦枪走火」成民族大冲突，仍旧是少数民族地区的大问题。<br />
<br />
