													<h2>陈丹青、梁文道昨现身党报论坛</h2>
						
						
																											<div id="postmessage_133017" class="t_msgfont">PS：这个不是转载，应该属于转述，想了半天还是先发到人气比较高的这里，还望书记手下留情。<br />
<br />
昨日（6月22日）下午，陈丹青、梁文道、马东三位嘉宾现身上海《解放日报》报社主办的文化大讲坛，以“多元视野的文化宽度”为主题发表演说，并与台下众媒体听众（以及夹杂其中的一小撮粉丝）进行了互动。<br />
<br />
在解放报社党委书记发表了欢迎词后，陈丹青先生首先登台，以“为什么宽容那么难”为主题讲述了一刻钟左右。他首先表达了竟然接到党报邀请的惶恐之情，同时表示不理解为何党报定下“宽容”的主题，认为讲“没有宽容该怎么忍耐”更为合适。其开场白迅速调动了台下听众的情绪。接着，陈先生从最近的一次土耳其之行中对土国国父凯末尔对反对党的宽容说起，直接提出当年胡适对蒋介石建议的“让反对党合法化”。陈先生说：“其实我有点同情蒋委员长。如果当年他接受了胡适的建议，就没毛泽东什么事儿了，也没邓小平什么事儿了。”不过，他同时也指出：“在中国，你对别人宽容，对方就会得寸进尺，然后就会吃掉你。中国没有双向宽容的传统。”“我不相信道德力量能让个人宽容，更不可能让政权宽容。但是我很感谢改革开放30年，无论是媒体、科技、网络都在发展，大家说话的空间更多了，你奈何不了我，我也奈何不了你，这才有可能出现宽容的苗子。”<br />
<br />
陈丹青先生在掌声中归座时，梁文道先生接了一句很有趣的话。他说：“我很惊讶你还能感谢改革开放。我在凤凰卫视做主持，台领导都不让我夸奖共产党。领导说每次当我夸奖我党，听起来都象在讽刺。”<br />
<br />
接下来登台的是马东。很抱歉由于本人的孤陋寡闻，我第一次知道马东是谁。他的主题是“文化从容和文化焦虑”（不确定，类似这个意思）。讲演大义是目前这个时代并不是文化的高峰期，只是一个走向繁荣的准备期。在讲演末尾，马东突然话锋一转，指出目前文化大体是从容的，唯一值得焦虑的是教育。目前的教育体制完全是荒谬的。但他并未就此进行展开，而是草草结尾。<br />
<br />
梁文道先生作为压轴嘉宾出场，主题为“中华传统文化的和谐与包容”。他开场即说，本来讲演的主题定为“儒家文化的异端”，但一方面这个题目太专深，另外主办方认为“异端”一次太敏感，于是他换成前述的标题，并且特意加了“和谐”这个关键词。在接下来的演讲中，梁文道主要谈及儒家历史上对异端的定义与包容。与西方基督徒不同，儒家虽然把不同于己的言论与人都称为异端，但其并没有一个中央化的权威机构来作为真理的唯一代表，去判定真理或审判异端。换言之，儒家并没有真理部，他们也不认为有人能掌握真理的解释权。相反，儒家鼓励人去看不同的东西。在他们眼里，和谐应该是“和而不同”。但今日的和谐更多的不是和，而是同。梁文道在讲演中主要是借古讽今，但并未怎么涉及实事。<br />
<br />
讲演结束后，开始台下听众与嘉宾的互动。看起来是场下解放日报社下辖不同的媒体记者在提问，但后来我发现递话筒的女生手上一直拿着一张纸。通过认识的记者借过来一看，发现所有的问题和提问人都是事先准备好的。总体来说，准备的问题大多比较弱智，比如问陈丹青“之前是以画家身份出现，现在是否要转做作家”（汗。。。），问马东“在你主持的文化访谈节目中，最印象深刻最感人的一次访谈是哪次”（马东赶紧拿起话筒澄清：我从来没在节目中哭过）。<br />
<br />
互动的第一次高潮是一名男士站起来提问，他首先介绍自己是《支部生活》的记者。陈丹青略为诧异的重复了一声“支部生活”，引起全场哄笑。该记者的提问大致关于目前不宽容的文化是为体制所决定，还是体制为文化所决定？陈丹青毫不留情地直接说：“就是由支部生活决定的。我们的很多语言都是支部的语言。”这一回答再次引起场内的掌声。<br />
<br />
互动的第二次高潮出现在整场活动临近结束。当时准备好的问题都已问完。这时坐在第二排的一个男生举手，拿到话筒后他站起来，明显有些紧张地说：“今天，我是作为陈丹青先生和梁文道先生的粉丝过来的。两位均作为公共知识分子，而不是画家或作家。。”刚说到这里，台上一直坐在一边的一名女主持人（话说这个女主持人从开场到那时几乎一句话都没说过，之前完全不明白她的功能是什么）突然拿起话筒，直接打断那个男生的提问，就好象他根本不存在似的，面向嘉宾说：“其实我还有个问题。”场面顿时尴尬起来。那个男生拿着话筒，说也不是，不说也不是，而嘉宾都还看着那个男生。那位女主持人则自顾自地接着说：“我还有个问题，最近流行娱乐至上或娱乐至死的论点，不知三位对此看法如何。”这时还是陈丹青提出，能否先让那个男生把问题说完。女主持人明显非常不甘心，但碍于陈的要求，只得同意。那位男生看起来更紧张了，说话都有些吞吐，接下来只是与陈丹青核实了他的博客是否已关闭，以及在饭否上的并非真身。最后他说：“接下来我要问的可能都是敏感词了。”接着交出话筒坐下，女主持人明显松了口气，场面重新得到控制。不过梁文道接下来说了句话：“回到娱乐至死的话题，我们现在怎么可能不娱乐呢？连公共知识分子都成为敏感词，不娱乐一点能做什么呢？”再次得到全场掌声。<br />
<br />
目前不清楚这个党报举办的论坛为何会邀请两位“危险分子”参与，基本上陈丹青的言论都秉承其一贯的大胆风格，梁文道则相对比较注意分寸。即使如此，“越位”的言论还是屡屡出现。我当时坐在第四排，实在看不清第一排那个党委书记的脸，不知道他们是不是肠子都悔青了。</div>

							
							
							
							
