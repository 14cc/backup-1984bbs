　　文强因涉嫌严重违纪被双规后，从其一处住所中搜出来大量的人民币、港币、美元、英镑以及金条，价值3800万元，还有8处房产（其中4处别墅），其总资产已近9位数。“重庆警方自6月以来端掉的14个大型黑恶犯罪团伙，其中多名首犯是重庆当地亿万富豪。”重庆一位政界人士认为，文强既甘作一些黑恶势力的保护伞，其收益自然不会低。<br />
<br />
　　从打黑英雄到黑势力保护伞<br />
<br />
　　2000年9月19日，中国头号悍匪张君在重庆被擒获，时任重庆市公安局副局长的文强成为出尽风头的英雄；可是仅仅在一个多月之后的10月25日，重庆查禁赌场的警察死于黑社会性质组织成员护赌的枪口下，虽原重庆市公安局治安总队总队长李虹等被判刑，但文强被指为幕后的“最大保护伞”。“从那以后，人大代表和政协委员对他的反映就没间断过。”<br />
<br />
万州是重庆主城区以东280多公里的一个区。在这里，人们也能感受到打黑两个多月以来带来的社会秩序明显好转。<br />
<br />
　　根据重庆市政法委、重庆市公安局的统一部署，万州警方内部目前已有包括网监科科长张新平在内的 4名警察落网，涉及网监科、出管办、技侦处、预审科四个科室。据报道，今年打黑风暴启动以来，整个重庆公安系统已有20多位处级以上官员因涉黑被掀翻落马，已处理警员共达100多人。<br />
<br />
　　公安局长会上被带走<br />
<br />
　　当地一些涉黑案件牵扯出的警界内部人员正是某些黑恶势力的“保护伞”。据警方内部人士透露，在陈明亮、黎强被捕后几小时内，他们的手机不断收到警示逃跑的短信，发信者多是警方内部人士。<br />
<br />
　　在上述万州区公安局正接受调查的4人中，包括一名公安局出管办民警熊绍禹。熊的落马，还牵出了邻近的忠县公安局政委张攀权。接近警方的人士称，2人的落马，是缘于去年忠县查处的一起赌博大案。有当事者拿出数十万元，希望借以“化解”牢狱之灾。万州民警熊绍禹被找到，而熊又找到了与其熟识的忠县警方政委张攀全。该当事者最后被放走。<br />
<br />
　　8月18日重庆市公安局召开的打黑除恶警示教育会会场上，垫江县政府副县长兼垫江县公安局局长徐强是当场被带走调查的3名处级领导干部之一。就在8月13日，他还亲自下乡摸底，布置工作，带领全副武装的10名特警、8名武警和当地派出所民警，在当地周家镇打掉了一个为恶一方的黑势力。<br />
<br />
　　多名公安干部被文强“所害”<br />
<br />
　　有消息称，徐强是因其此前在九龙坡区公安分局任副局长期间对黑恶团体实行按照效益“收税”而被双规。“他在九龙坡区的时候对黑恶势力很保护，按照他们每年的效益收税，明码实价，对几个黑团体还一视同仁。”一名消息人士称。也有消息称，徐强系因受文强案牵连。另一个被广泛认为受文强“所害”的是重庆市公安局经侦总队总队长陈光明。陈光明在重庆是赫赫有名的人物，党的十七大代表，全国十大女杰，全国劳模，也曾是重庆乃至全国省级公安禁毒战线上惟一的女总队长。 <br />
