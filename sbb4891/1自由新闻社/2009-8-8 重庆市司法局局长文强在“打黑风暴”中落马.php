																											<div id="postmessage_208841" class="t_msgfont">2009年8月7日早晨，从北京飞往重庆的国航CA1419航班，在不经意间执行了一次特殊任务。 <br />
 <br />
据飞行员事后记述，“登机后乘务长就被告知，飞机上有特殊旅客。但当时只透露说是7个警察跟踪1个嫌犯。”<br />
<br />
　　飞机于9点38分在重庆江北机场落地，10分钟后靠桥开门。一名重庆本地记者隔着驾驶舱玻璃，用相机拍下了令他吃惊的一幕：两辆商务车夹着一辆警用防暴车，停在离飞机约20米处。重庆市司法局局长文强，在一群便衣警察的簇拥下被带上中间的防暴车。此前一天，他还在北京参加全国司法厅局级会议。而押解他的便衣之中，其中一位就是素有“打黑英雄”之称的重庆市公安局局长王立军。在准备上车前，面对记者的拍照，文强摆出了自己标志性的动作：双手交叠放在胸口，看起来跟之前他视察和调研时的新闻照片没什么不同。<br />
<br />
　　“据中共重庆市纪委有关负责人证实，重庆市司法局局长文强牵涉严重违纪，目前正在接受组织调查。”7日23点26分，这条官方消息由华龙网发布。<br />
<br />
　　但在20个小时前，就有网友在天涯社区的重庆论坛发帖说：“今日凌晨2点半，市委决定，因涉黑问题，对市司法局局长文强进行双规。”<br />
<br />
　　网友发布的“权威消息”被一些重庆市民过早采信了。8月7日，一位重庆市民在渝北区黄泥磅黄龙路重庆市公安局所在地，看见地上散落着好多类似传单的纸张，上面写着：“庆祝重庆黑老大文强被抓！”而重庆的一些街巷里也响起鞭炮声。<br />
<br />
　　这无疑是重庆最近掀起的“打黑风暴”中一个关键节点，之前曾在网络上担心“这次扫黑会不会只是走个过场”的重庆人开始确信，“是动真格的了”。<br />
<br />
　　《中国新闻周刊》记者获悉，截至8月19日，重庆市已有众多警察接受调查。如果仅从数量上看，这并不能说明此次打黑风暴有什么特殊――2007年，重庆市委政法委书记刘光磊曾对媒体透露：直辖10年以来，重庆已经有近1000名违法、违纪民警被处理。<br />
<br />
　　这一切，也许只是个开始。<br />
<br />
　　被捕原因之一：赌场“保护伞”<br />
<br />
　　“文强的案子在查，但现在为止谁都还不清楚。”该案一位核心知情者对《中国新闻周刊》记者表示，“抓文强，肯定是做了大量工作、掌握了一些证据，目前还没有形成证据链。”<br />
<br />
　　而据《中国新闻周刊》记者了解，文强被捕至少和2000年的“白云湖案”有关。<br />
<br />
　　1999年下半年，重庆著名的“黑老大”王渝男等8位股东合资，在璧山县白云湖度假村开设“百家乐”地下赌场。为寻庇护，他们先后“搞定”了赌场所在的青杠镇派出所所长冉勇、治安总队副总队长龙蜀渝、一科科长陈渝、总队特业科科长汪德泉，直至治安总队总队长李虹。<br />
<br />
　　2000年10月2日，白云湖赌场被查，却在短短半个月之后就重新开放。6天后，赌场再次被便服潜入的警察查抄，一个叫张荣彪的马仔为了保护码房，隔着门板向警察开枪，猎枪的子弹穿过门板从左肩胛骨射入民警王诵伦体内，随即散开成20多块弹片，王诵伦当场身亡。<br />
<br />
　　当时被上级指派负责查这个案子的李虹，私下让人毁掉了账本和通讯录。一位参与此案诉讼的司法界人士向《中国新闻周刊》记者回忆说，“当时我看卷宗的时候就注意到，王渝男他们开设赌场期间，每天都从码房拿出4万块钱进行‘活动’，但由于账本被毁，后来法院也没法追查这笔钱的去向。”<br />
<br />
　　这个案子的结果是，其余4名涉案民警都被判刑，只有李虹的名字未见于判决书。但他后来却因“虚开发票”的罪名被判了6年。李虹出狱后的去向，《中国新闻周刊》记者听到两种说法，一说在江北区开茶馆，一说在沙坪坝区开饭馆。<br />
<br />
　　知情人透露，李虹是由文强一手提拔起来的，担任治安总队总队长之前，负责打黑的刑警总队一支队任队长。而据重庆市公安局原副局长对《中国新闻周刊》记者的说法，白云湖赌场在第一次被查时，被扣了很多现金和80多辆车，当时打招呼要求放人和退还赃款赃物的，正是文强。<br />
<br />
　　调任司法局<br />
<br />
　　文强今年54岁，前公安一级警监。据熟悉他的人描述，这位司法局前局长身高约1.65米，身材偏胖，腰上常喜欢系一条LV品牌的皮带，这条皮带也由此不时出现在新闻照片中。尽管皮带造型的时尚感与他的西裤看起来并不协调，但他似乎并不以为意。<br />
<br />
　　这也符合曾经提拔过文强的老领导、重庆市政协原主席张文彬对他的印象：“在市里与文强的工作接触很少，后来偶有接触，从文强的言谈间感觉他有些自傲，甚至是放肆。”张文彬与文强原来都在巴县(今重庆市巴南区)工作，先后被调进市里。<br />
<br />
　　张文彬最后一次见到文强大概是一年前，那时，文刚从市公安局常务副局长调任司法局局长不久。两人参加同一个会议，张问文忙不忙，文答，晚上没有在公安局时那么紧张，但白天还是很忙的。<br />
<br />
　　但这个职务的调动对文强的影响，很可能比他自己感受到的更为深远。2008年6月25日，重庆市公安局宣布人事任免的决定，从辽宁锦州调来的“打黑英雄”王立军接任了文强重庆市公安局常务副局长的职务，文的新去处是重庆市司法局。<br />
<br />
　　重庆知名律师韩德云分析，作为正厅局级侦察员，文强调任司法局局长属于平调，他虽由此从“二把手”变成了“一把手”，却离开了公安局这个业务部门。新去处司法局，在职权范围上主要处理司法领域里的一些行政性事务。而最为关键的是，调动让文离开了自己经营多年的公安系统。事后回溯，这被不少热衷于摆龙门阵的市民认为是“要动文强的信号”。<br />
<br />
　　类似的传言在重庆并不鲜见。一位当地媒体记者6月就曾听到这样的风声，他试探性地给文强打电话。电话那头，文强的答复体现出一种建立在自信基础上的幽默感：“我也听说我被双规了。”<br />
<br />
　　韩德云则用“直率”来形容他所接触到的文强，“他不是那种会拿着报告念的人，不喜欢讲官话。重庆市规定干部在公开的会议上讲普通话，但文强在开会时却说重庆话，这在重庆的厅级干部里很少见。”曾任巴县县委书记的李兵也评价文强，“敢于发表自己的意见”，“说话直，有些人会认为他说话很冲”。<br />
<br />
　　江湖义气<br />
<br />
　　但 “一般人很难接近”的文强的另一面是，在公安局内部，一些跟他相熟的警官并不以职务相称，而是亲热地称他“强哥”。一位对文强有所了解的“社会人士”称他很讲义气，喜欢照江湖规矩走，不按法律办事。在任副局长时，公安局的管理比较松散，“只要你能把事情办成，怎么做他不管”。而遇到警匪对峙的场面，他会在枪林弹雨中亲临前线指挥，不是那种躲在后面的人。<br />
<br />
　　“有的同志问我，文强是你选上来的，他平时来不来你这边，我说，不来。”张文彬叹了口气对《中国新闻周刊》记者说，文强过去在巴县很规矩的，到了市里以后，说话也开始不拘小节，不时口带脏话。<br />
<br />
　　一位前警察回忆，90年代的时候，重庆黑老大王平的女儿过生日，文强很高调地公开亮相，“他穿着一身黑，开着名车，带着几名警察做保镖，大摇大摆地就去了”。不久，王平因涉黑被通缉。<br />
<br />
　　没有人知道文强是否了解这份张扬究竟意味着什么，但这确实曾给他带来麻烦。一个未经证实的说法是，2005年，曾有人拿着文强与王平的合影威胁他，公安系统也曾找他谈过话，文强的答复是，以“要培养特勤”为由，把自己“择”出去了。<br />
<br />
　　一位在重庆商界混迹多年、被传与诸多“黑老大”打过交道的人士对《中国新闻周刊》描述：这种江湖义气的形成与巴蜀地区的地域文化有很大关系。那里流传着久远的“袍哥”文化，源出诗经“岂曰无衣，与子同袍”之意。就是兄弟之间“讲豪侠、重义气、急人之急”，甚至可以到“汗衫大伙穿，婆娘大伙睡”的所谓袍哥义气。而这位前打黑英雄的落马，不能说与这种“江湖义气”毫无关系。<br />
<br />
　　9年之前，文强曾经是显赫一时的打黑英雄。在他的指挥下，“中国第一刑案”要犯张君伏法。在1994年至2000年，长达7年的时间里，张君在渝、湘、鄂、桂屡屡杀人越货，却丝毫没有给公安局机关留下侦破线索。<br />
<br />
　　而文强在此期间，与张君整整打了6年的交道。<br />
<br />
　　直至2000年9月1日，以张君为首的暴力犯罪团伙在湖南省常德市持枪抢劫运钞车，枪杀7人，致伤5人，制造了新中国成立以来死伤人数最多的暴力抢劫特大案件。案发后，渝、湘、鄂三地警方联手，最终在重庆，张君折在了文强手里。<br />
<br />
　　在这场耗时6年、耗资1000万的持久战中，不应忽略包括文强在内的指挥者的作用，据重庆市公安局《抓捕悍匪张君的日日夜夜》一文记载：“在整个侦破过程中，重庆警方排查了流动人口150多万人，调查访问了11790户，与13510名群众见面，调查了6263名出租车驾驶员，清理治安复杂场所1700余处，比对指纹380万枚。”协调指挥如此大规模的一场抓捕，对文强的意志和能力显然是一个不小的考验。<br />
<br />
　　此前，文强手底下也办过几个过硬的大案。1992年，重庆警匪枪战；1994年中国第一盗案；重庆的抢劫运钞车案&#8943;&#8943;他破获的若干案件被公安部记一等功。<br />
<br />
　　当地一位资深记者认为，文强还有爱表演的一面。这位记者提及让文强扬名立万的抓捕张君现场的一个细节，“张君明明已经倒在地上，被干警控制住了，他还冲上去朝着张君的脸踩了一脚，当时有摄像机对着他。”<br />
<br />
　　37岁的副局长<br />
<br />
　　关于文强过往的辉煌战绩，媒体的报道集中于1992年重庆警匪枪战、1994年中国第一盗案和抢劫运钞车案。但据《重庆法制报》的一位老记者回忆，1987年，在重庆底下的一个区县，出了重庆自建国以来比较大的一个案子，当时公安部一天3个电话来催，重庆市调动了1000多名民警，跨云贵川三省追捕逃犯，这个案子是在文强的领导下破获，《法制日报》为此作了两个整版的宣传报道，在重庆市影响非常大。这也是他能够升调到市局的一个比较大的资本。<br />
<br />
　　张文彬说，自己之所以选择提拔文强，有两个原因，一是当时中央提倡“干部年轻化”，更关键的原因在于，文强的业务能力和思想作风在同年龄段的干部里表现最为突出。据张介绍，1972年参加工作的文强是从泸州公安学校毕业的，毕业后被分配到巴县公安局秘书股，刚进来时是普通工作员。人年轻、聪明，对工作肯钻研，工作上有成绩，很快升到了科级干部。后来，县里选拔年轻干部，政法口选的是他。<br />
<br />
　　凭借在1982年起的全国严打斗争中的抢眼表现，文强进入了时任巴县县委书记的张文彬的视野。当时文强是严打工作班子的成员之一，作为县委书记，张文彬经常听取汇报，在此过程中发现了文强并对其有所了解。但在重庆市公安局一位原副局长的眼里，文强的工作能力并没有那么出色，“19世纪70年代末有一回在涪陵出差，他把枪给丢了，还是我们给帮着作的检查”。<br />
<br />
　　1983年，巴县搞机构改革，组建新班子，恰逢县委分管政法口的常委到成都学习两年，文强就接替了他的职务，得以跻身县委常委，此时，他连县公安局副局长都不是。1985年左右，年仅30岁的文强升任巴县委副书记。<br />
<br />
　　1991年，重庆市公安局负责人看中文强，想调他来市里，开始提出建议让他升任沙坪坝区公安分局局长，但当时的巴县县委主要领导认为这个职务安排太低，“我们巴县出来的干部，没这么安排的”。隔了一段时间，到了1992年，市公安局提出，文强不到区分局了，直接提为市局副局长，至此，37岁的文强完成了其仕途的第二次飞跃，正式成为重庆市公安局分管刑侦的副局长。<br />
<br />
　　1997年重庆直辖，文强一路顺风顺水地升任市公安局副局长。2000年11月，提任正厅局级侦察员。2003年任局党委副书记，直至2008年调任司法局。<br />
<br />
　　8月14日，重庆市政府新闻发言人周波称，“文强因严重违纪已被双规，充分证明了市委市政府打黑除恶的决心，不管背景有多深，关系有多复杂，经济实力有多大，只要侵害了党和人民的利益都将一查到底，绝不手软。”<br />
<br />
　　这种“拉网式全天候的密集打击”，不仅震慑涉黑团伙，对于公安队伍中的违法违纪者也同样有效。此前，公安系统内部盛传，“有问题一定要去自首，不要等组织来查。”</div>

							
							
							
							
