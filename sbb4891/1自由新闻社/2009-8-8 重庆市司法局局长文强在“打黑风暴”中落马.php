																											<div id="postmessage_208844" class="t_msgfont">“不只是一个文强的问题”<br />
<br />
　　2008年6月，“打黑英雄”王立军空降重庆之前，在重庆的官方语境中，对城市治安的表述是，“根据统计，我市群众的安全感最近几年一直都保持在92%左右，2007年达到了最高峰的93.73%”。 <br />
 <br />
<br />
　　但与此同时，重庆屡发涉黑、涉枪大案，整体治安形势严峻，这直接导致文强在当地民众中一直口碑不佳。根据重庆市公安局的统计，重庆近年来治安案件每年在10万件以上。<br />
<br />
　　一位司法界人士认为，如此多积案，加上扫黑专项斗争所暴露的重庆黑社会势力猖獗的事实，“作为分管刑事侦查这么多年的公安局负责人，文强应负主要责任”。<br />
<br />
　　2009年6月起，重庆下了狠心，一举端掉了14个大型黑社会犯罪团伙，8月初，任公安局副局长11年之久的文强因涉黑被双规。<br />
<br />
　　在近50天的专项集中整治之前，重庆市委成立了“打黑除恶”专项斗争领导小组，政法、纪检监察、经济三条战线联手，令重庆的命案侦破率达到了91.35%，高于全国平均水平2.95个百分点。重庆官方还表示：破命案积案超过过去5年的总和，万人刑事案件发案率低于京、津、沪。<br />
<br />
　　目前，已取得初步胜利的“打黑除恶”行动仍在继续。“这其中有我们欠社会的账，欠老百姓的感情账。”王立军对他的同事们说。而在网络上，许多人――包括一些外地人，也正在热烈呼吁“王青天”的降临。<br />
<br />
　　萧条的是老杨和他的洗脚房，这次严打，好像漫长过任何一个冬天。不时有男子往里面探头望望，老杨不住地赔笑脸，“不好意思，停业整顿，改天来耍嘛。”<br />
<br />
　　但其实老杨自己也不晓得“改天”是哪天。他有种不好的预感。据说，这栋近20年的违章建筑，很快就要被拆除了。<br />
<br />
　　薄熙来治渝“红与黑”<br />
<br />
　　扬“红”打“黑”，两场运动正激荡着重庆闷热的空气。<br />
<br />
　　本刊记者 / 周政华 (发自重庆)<br />
<br />
　　位于重庆的著名红色景区“红岩联线”迎来了人流的高峰。2009年8月16日一天里，这个包括渣滓洞、白公馆等景点的旅游线路共接待游客约1.6万人，比去年增长了1/3。“今年的人多了很多。”一位“红岩联线”的工作人员对《中国新闻周刊》表示。<br />
<br />
　　红色经典被热捧，与中共重庆市委市政府的施政方针不无联系。主政山城600多天后，中共中央政治局委员、重庆市委书记薄熙来掀起的“唱红歌、读经典、讲故事”渐入高潮，与此同时进行的，是震动全渝的“打黑除恶专项斗争”。<br />
<br />
　　重庆官方表示：到8月中旬，已经有3298万人次参与了“红歌传唱”活动，而落入法网的涉黑成员多达1500余人，另有50多名官员因贪腐入狱。<br />
<br />
　　扬“红”打“黑”，两场运动正激荡着山城闷热的空气。<br />
<br />
　　“红色热潮”<br />
<br />
　　解放前，重庆是中共地下党最为活跃的城市之一。眼下，中共元老之一薄一波的儿子薄熙来，试图重新唤醒人们对这段红色历史的关注，以服务于一系列政府目标。<br />
<br />
　　作为中国西部唯一的直辖市，重庆位于群山之中。受惠于上世纪60年代的“三线建设”，重庆成为国内六大老工业基地之一，建立了以装备制造业、汽车摩托工业为主的重化工业体系。但受限于地理位置，重庆经济的外向度较低，引进外资、进出口贸易额长期落后于临近的湖北、四川等省份。<br />
<br />
　　针对这一现实，薄熙来呼吁重庆要“建设中国内陆的开放高地”，并提出了“宜居、畅通、森林、平安、健康”重庆五个目标。重庆官员则普通认为，薄熙来主政大连时期的一些成功经验将被移植到重庆。<br />
<br />
　　1992年～1999年，薄熙来任辽宁省大连市市长时，将这个曾经沉寂的东北老工业基地，转型为充满活力的港口城市，并频频向外界推荐“大连城市名片”。由此，薄熙来也成为了“明星官员”之一。<br />
<br />
　　不过，与当年赴任大连市长不久即提出“市长经营城市”的思路不同，薄熙来这次选择了“提精气神”入手。<br />
<br />
　　对于薄熙来的这一倡议，重庆市委宣传部理论处长苟欣文援引薄熙来后来的一次讲话说，“动员大家唱红歌、读经典、讲故事，就是要培养重庆人民良好的精气神。有了思想内涵，有了文化品位，重庆就能持久，就有合力，就有实现跨越发展的可能。”<br />
<br />
　　“大城市、大农村、大库区”的市情，使得重庆要实现“跨越式发展”困难重重。由于境内3000多万人口中近70%为农民，其中150万仍在贫困线之下挣扎。2008年，重庆的经济增速虽然高于全国平均水平，但其人均国内生产总值仅为成都市的一半左右。<br />
<br />
　　外界揣测：薄熙来主政重庆后首抓“提精气神”，不仅源于前述重庆发展所面临的长期问题，还可能与其“红色家庭”出身背景有关。<br />
<br />
　　2008年5月30日，薄熙来在调研重庆学校工作时，就发出了一个倡议：在全社会形成高唱红色经典歌曲的热潮。<br />
<br />
　　不久，重庆市委宣传部就选取了《义勇军进行曲》《红太阳》等27首革命历史歌曲和《我和我的祖国》《难忘今宵》等18首改革开放以后的现代经典歌曲，并以下发政府红头文件的方式向市民推荐传唱上述歌曲。<br />
<br />
　　由于前述文件提倡重庆传唱红歌以合唱为主，不久重庆市级党政机关、40个区县直属机关纷纷成立合唱团，都将唱红歌作为重要纪念日的标志性活动，随后大、小红歌演唱会密集举行。<br />
<br />
　　重庆电视台新闻频道甚至专门开设了“红歌社区行”栏目，海选、PK、现场直播、明星参与等方式也被引入到红歌传唱中来。重庆市歌剧院院长张礼慧认为，红歌对于重庆这座红色城市来说，可以振奋凝聚人心。<br />
<br />
　　多名参加了红歌合唱团的重庆市公务员在接受《中国新闻周刊》采访时表示，单位花十几个小时练习合唱，忙的时候周末也得搭进去。<br />
<br />
　　除了红歌传唱，被重庆坊间称之为“红色热潮”的还有“读经典、讲故事”。<br />
<br />
　　今年，重庆市委宣传部出版了一套《读点经典》丛书，收录有革命烈士方志敏散文《清贫》，以及叶挺写于1942年的《囚歌》等红色作品。薄熙来为这套丛书所作的序言中写道：“经典应是古今中外的文化精华、传世之作，而引领中国走向独立、富强的共产党人，也在他们书写英雄史诗之时，留下了许多催人奋进、感人至深的文化经典。”<br />
<br />
　　越管越细<br />
<br />
　　与“唱红歌、读经典、讲故事”的文化路线相比，让重庆官员感觉压力大增是薄熙来对官员考核标准的提高。<br />
<br />
　　这同样被认为是他大连施政方针的延续――他曾经公开表示：“如果有一百个叫硬的干部都能哈下腰干活，而且忙到点子上，那大连就一定会兴旺起来。”<br />
<br />
　　“学历的重要性从未如此强调过。”重庆市开县的一位年近50岁的乡镇干部在接受《中国新闻周刊》采访时说，他刚刚报了市委党校的一个大专班。<br />
<br />
　　今年8月1日，重庆市人保局会同市委组织部、市教委、市招办联合下发了《关于进一步加强基层机关事业单位在职人员学历教育工作的通知》称，截止到2012年12月31日，没有取得国民教育大专以上学历的人员，将视为不能完成工作任务予以辞退处理。<br />
<br />
　　除学历外，官员应对群体性事件的能力，也被视为考察行政能力的重要标准之一。去年11月重庆曾爆发震惊全国的出租车“罢运”事件，一度导致重庆交通瘫痪，后来薄熙来赶到现场与罢工司机对话，并通过电视直播。对此，清华大学公共管理学院院长薛澜认为，重庆市政府在解决罢运事件上确实为地方政府做出了示范。<br />
<br />
　　官员政绩的考核项目也有所增加。今年初，《重庆晚报》报道了该市一些不文明养犬的行为，有人大代表、政协委员认为，《重庆市养犬管理暂行办法》形同虚设，相关规定并没有得到落实。不久，市政府新闻发言人周波回应说，市政府将加大考评力度，强化工作指标，拟将各区县养犬管理工作纳入年终政府工作考核，并将各区县养犬管理工作的成效作为相关责任人年终综合性评先的重要依据。<br />
<br />
　　“狗管得好不好也要纳入区县考核，这在以前想也想不到。”重庆市渝中区一名法官对《中国新闻周刊》表示，政府对于管理越来越细致。<br />
<br />
　　反映这种细致管理的另外一个例证是：去年8月25日重庆市政府办公厅发出通知，要求该市机关、企事业单位每天上下午各做一次操，每次20分钟左右。据悉，各单位的工间操和课间操做得好不好，将作为所在区县政府体育工作的考核内容。这也被认为是薄熙来此前提出的建立“健康重庆”的举措之一。<br />
<br />
　　重庆官员更大的震动，来自于市委市政府的大力反腐。在过去的一年里，包括重庆开发区管委会原主任唐文峰、市规划局原副局长梁晓琦、九龙坡区区长黄云、市规划局局长蒋勇、重庆国土房产局原副局长王斌、渝中区副区长王政、渝中区委副书记郑维、沙坪坝区副区长陈明等多达9名厅局级官员和10多名处级干部因身陷“地产窝案”纷纷落马，一时震惊重庆官场。<br />
<br />
　　接近重庆公安局的人士称，截至今年7月，重庆已有502名公检法干部涉案接受调查。<br />
<br />
　　打黑风暴<br />
<br />
　　出租车“罢运”和系列腐败案中所暴露出的黑社会势力渗透的蛛丝马迹，促使薄熙来把建设“平安重庆”演变为一场持续两个多月的“打黑除恶专项斗争”。<br />
<br />
　　今年6月初，重庆市委成立了打黑除恶专项斗争领导小组，政法各部门、纪检监察、组织、宣传、工商、税务、银行等均参与联动，公安机关组织了15个专案组重点突破。<br />
<br />
　　8月8日，重庆市纪委证实，现任重庆市司法局局长的文强涉嫌严重违纪，目前正在接受组织调查。<br />
<br />
　　重庆市公安局称，截至8月15日，已成功抓捕黑恶团伙成员1544人；收缴枪支48支、子弹877发；查封、冻结、扣押涉案资产15.3亿元。累计破获查处各类案件1009起，其中破获刑事案件892起。<br />
<br />
　　“可以说是打了一场真正的人民战争。”重庆市政法委书记、市打黑除恶专项斗争领导小组组长刘光磊8月15日称，在这次“打黑除恶专项斗争”中，群众向公安机关提供线索9165条，其中80%都是实名举报。<br />
<br />
　　7月31日重庆市一次通报会上，重庆市公安局局长王立军表示，重庆一些黑恶组织已有“合法”外衣，以商养“黑”，以“黑”富商。<br />
<br />
　　这也带来了担忧：政府中还有多少官员充当黑社会的保护伞？薄熙来在8月7日主持召开的市委第三次打黑除恶工作指挥调度会上说，涉及党政干部、政法干警的管理要及时研究，将不良分子甄别出来，纯洁队伍。</div>

							
							
							
							
