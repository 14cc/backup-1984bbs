													<h2>广电总局主任怒指王璐瑶在“演三级”</h2>
						
						
																											<div id="postmessage_442059" class="t_msgfont">广电总局批主持人太低俗 衣着暴露打情骂俏(图)<br />
 来源：人民网 2010年01月20日<br />
<br />
　　昨天，“星光”电视主持艺术论坛在郑州举行。论坛由李瑞英主持，撒贝宁、董浩、张泉灵等围绕“主持人角色意识与职业精神”的主题进行研讨。国家广播电视总局收听收看中心金文雄主任发言，指出了主持人存在的种种问题并且点名批评，他说：“最容易把电视节目做低俗的是一些&quot;游走型主持人&quot;，或者说演员、明星主持人，比如侯耀华、王璐瑶、朱时茂、李斌、刘仪伟、大兵。”<br />
<br />
　　吹捧过度管赵忠祥叫爹地<br />
<br />
　　论坛主办方中国电视艺术委员会，于会后向媒体提供了金文雄的发言记录。<br />
<br />
　　金文雄在发言中点名批评了几个低俗节目，他表示，广电总局非常重视主持人的素质，因为低俗倾向有一半责任在主持人身上。<br />
<br />
　　“现在的主持人存在很多问题，首先是主持人失语或者乱语。比如很多情感故事类节目，主持人观点性的点评不到位。第二是越位，就是不该主持人说的、做的，主持人去做了。比如有一个节目叫《伸向聋哑孩子的罪恶》，这是一起非常严重的刑事案件，但是这个节目组不报警，还去采访、追踪、破案，最终一无所获。还有一个节目叫《23年离婚进行时》，节目组竟然把一男子带到医院检查，主持人在现场宣布诊断结果。第三，主持人失语，很多家庭纠纷节目，经常打起来，主持人不但不劝解，还故意煽风点火，这些节目是以争吵打骂为噱头，比较低俗。比如《谁才是第三者》还有人要当场割腕，主持人不制止也不调解。”<br />
<br />
　　金文雄说，还有一些栏目吹捧嘉宾过度，“比如《面孔》节目，介绍赵忠祥时，称他为国脸，然后吹捧了五分钟。以前中央电视台也做过类似的节目，也有吹捧的。有些主持人叫赵忠祥爹地，还是叫老师比较好。”<br />
<br />
　　穿得太少一出场就打情骂俏<br />
<br />
　　针对主持人的着装问题，金文雄也提出了看法。他说主持人要注意外表，最近新闻节目主持人也佩戴饰物，很亮，灯光一打很晃眼，还有一些综艺节目主持人的头发五颜六色。<br />
<br />
　　“穿着方面广电总局没有太明显的规定。低胸低到什么程度，像台湾是到第二个纽扣，现在有很多主持人低得很多。”他举例说，一次接到领导电话，说有一个节目的主持人着装有问题，把带子找回来一看，确实穿得非常不雅，粗网纹的袜子配黑皮裙，上身基本没穿衣服，堆着丝巾。“领导说穿得太不像话，主持人穿成这样确实不应该。”<br />
<br />
　　金文雄说，还有主持人一上来就打情骂俏，或者相互攻击，要么就相互吹捧，还有时候语言比较低俗。<br />
<br />
　　内容太黄像三级片<br />
<br />
　　金文雄说，有些主持人参与所谓的健康节目涉性话题，有时候介绍一些性经验、性技巧。“我们看到一个特别黄的节目是王璐瑶主持的，演员是在歌厅找的，有点像三级片。”<br />
<br />
　　金文雄说，侯耀华等明星主持过卖假药的节目，另外还有些主持人很难管理，“像刘仪伟、大兵，经常在游走，所以你看他走到哪，准保那个节目就低俗，这是我们跟踪的。对游走主持人我们跟踪比较紧，还有演员、明星。”<br />
<br />
　　金文雄指出了当下主持人存在的10多个问题，比如语言暴力、引导低俗、行为失度、经常犯傻。他举例说，“一个节目中，观众说上海人不好，主持人就对他说&quot;你团成一团然后圆润地离开&quot;，实际上就是让他滚。后来很多网民不干了，最后这个主持人公开道歉”。（记者赵楠楠） (来源：人民网-《京华时报》)</div>

							
							
							
							
