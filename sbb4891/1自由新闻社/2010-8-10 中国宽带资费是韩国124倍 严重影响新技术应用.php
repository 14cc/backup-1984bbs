&nbsp; &nbsp; 我国宽带资费是韩国的124倍 <br />
　　国家信息中心信息化研究部日前发布的一份报告称，我国主要信息产品与服务的资费水平依然偏高，这影响了信息技术普及和应用效果，成为信息社会快速发展的巨大阻力。<br />
<br />
　　这份题为《中国信息社会发展报告2010》的报告称，以互联网为例，当前我国互联网存在“速率低”的问题，远远达不到宽带的速度标准，如果考虑到网速问题，则我国的互联网应用成本更高。<br />
<br />
　　据统计，2008年， 我国上网接入速率约为1 .8 Mb ps， 远远低于日本的63 Mbps， 韩国的40M bps以及中国香港的20M bps。2008年，我国宽带用户平均月资费83.8元，相当于每Mbps每月46.6元(约合6.7美元)，是韩国宽带价格(0.37美元)的18倍、日本(0.13美元)的51.5倍。<br />
<br />
　　“如果考虑到收入差距的话，2008年韩国人均国民收入是我国的6.9倍，这意味着我国的宽带资费水平相当于韩国的124倍。”报告说。<br />
<br />
　　报告认为，当前我国的宽带资费对于国内绝大多数地区的收入水平来说都是明显偏高的。以上海最近推广的10兆宽带服务为例，月资费259元，相对于上海城市居民人均可支配收入的11%和农村居民人均纯收入的25%，相对于全国城市居民可支配收入的18%和农民人均纯收入的60%。<br />
<br />
　　报告指出，如此高的资费水平，将严重影响到新技术的进一步扩散和深化应用。(记者　陈伟　北京报道&nbsp;&nbsp;)<br />
