<br />
<br />
去年，毛新宇还为此做过调查。惊喜地发现在广州、深圳等地，农民工子女可以在城里免费读书，享受与城市人一同的待遇。他说农民工太辛苦了，为城市的发展作出了巨大贡献，城市不能给农民工子女就学另设门槛。去年，他还写了篇生态环境建设的提案，国家环境保护部还专门给过回复。对于房价过高这一问题，他表示正在作调研，不久将会写入他的提案中。<br />
