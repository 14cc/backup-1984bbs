人民日报社新大楼<br />
<br />
<br />
<br />
最近进行的人民日报新大楼设计竞赛经过多轮评审，最后由东南大学建筑学院周琦教授主持设计的方案获得第一并最终中标。<br />
<br />
<br />
<br />
&nbsp; &nbsp; 中标方案主要设计概念是以圆形造型表达“天圆地方”的寓意，通过电脑模拟从圆形开始逐渐演变成三维动态的双曲面造型，并且与长方形的基地形成呼应。最后在顶端形成的三角形的人字，与人民日报的主题关联，圆形的统摄地位同时满足了该项目作为区域中心的定位要求。<br />
<br />
<br />
<br />
&nbsp; &nbsp; 建筑从三个“角”上以外凸浑圆的巨大钢柱沿弧线直接交汇到楼顶，三面的玻璃幕墙则是内凹的曲面，整个造型简洁，并且十分节约造价。<br />
<br />
<br />
<br />
&nbsp; &nbsp; 东南大学建筑设计研究院凭借此方案赢得了与其它4家设计单位（中国建筑设计研究院，北京市建筑设计研究院、中南设计院、中冶设计院）之间的竞争，这也是该校建筑学科近五十年来首次在北京获得国家级机构的设计项目。<br />
<br />
<br />
<br />
&nbsp; &nbsp; 人民日报社新大楼将落成于北京东三环，处在北京市CBD（中央商务区）东扩的核心位置，与新近落成的中央电视台（CCTV）大楼遥相呼应。总体建设规模达到十多万平方米，由国际会议中心，图书馆，报业大厦等一系列建筑所组成。其中塔楼的建筑高度超过了150米。这是人民日报社成立以来最重大的建设项目，将会受到各方面的广泛关注。</div>

							
							
							
							
