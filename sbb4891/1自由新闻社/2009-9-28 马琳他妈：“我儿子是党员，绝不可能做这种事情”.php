<br />
　　昨日北方有媒体称，马琳和张宁益感情破裂，是因为马琳有了第三者，张宁益经纪公司为了帮张宁益在离婚官司中争取更多利益，还雇人跟踪马琳，试图偷拍马琳和“小三”幽会的证据。<br />
