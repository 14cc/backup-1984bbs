<br />
　　“马琳的个性，其实人们也都了解，他比较低调，不爱张扬。他是运动员，受人关注靠的是成绩。可张一总拿与马琳的感情说事，嘴上虽说不靠马琳，但她的言行谁都看得出来是怎么回事。对此，全家人都很反感。”夏女士说，“现在想起来，张一一直都在利用马琳，太有心机了”。<br />
<br />
　　“两人因为离婚的事都已经闹上法庭了，可是，这个时候张一还在说马琳要和她举行婚礼，这不是太荒唐了吗？张一这么说是想干什么，是不是还想利用马琳炒作？马琳这次之所以公开揭穿，那是实在忍无可忍了。”<br />
<br />
　　马琳捐款给灾区，她居然阻拦<br />
<br />
　　夏女士说，哪个母亲也不希望看到儿子在事业有了成绩之后家庭却出现了问题。“虽然一开始我就很反对，但后来一想，既然儿子喜欢，做家长的怎么能不尊重儿子的选择？可没想到，与张一接触时间越长，我们就越发现这女孩不行。”夏女士举例说，“马琳随中国乒乓球队去四川灾区时，向灾区捐了10万元，这本是一件很有爱心的事，但张一知道后却和马琳大吵一架，认为马琳没必要出风头。”<br />
