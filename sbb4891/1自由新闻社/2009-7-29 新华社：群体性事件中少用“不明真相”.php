																											<div id="postmessage_182044" class="t_msgfont">群体性事件中少用“不明真相”&nbsp;&nbsp;<br />
新华社北京７月２８日电 新华社记者黄冠 <br />
<br />
&nbsp; &nbsp; 近年来，一些地方在发布有关群体性事件的信息时，爱用“不明真相人员”“不明真相群众”等词句。日前，吉林通钢集团通化钢铁股份公司发生群体性事件后，“不明真相”一词再次被反复提及。这一说法耐人寻味，值得反思。 <br />
<br />
&nbsp; &nbsp; 首先，如果说群体性事件的发生是“不明真相”的群众被少数“别有用心”的人蛊惑、挑动的话，那么在事件发生之前为什么不能让群众知道“真相”？或者在事件酝酿阶段和刚露苗头之时，用公布的“真相”平息群众的疑问，把群体性事件化解在萌芽状态？说群众“不明真相”，并不等于真理站在了自己一边，反而暴露有关部门没有尽到应尽职责。 <br />
<br />
&nbsp; &nbsp; 其次，指责参与群体性事件的群众“不明真相”，无异于就说老百姓没有分辨事非的能力，这与事实不符。不容否认，有些事件确是因信息不实造成一些群众盲目参与，但即便是这样的事件，一般也有其发生的深层次原因。瓮安事件最终证明当事人是因溺水死亡，但根子上却与当地侵犯群众利益的事情屡有发生，群众安全感指数很低，干部作风粗暴、工作方法简单相关。同样，通钢事件是不是也有企业在重组过程中，没有照顾到职工利益的问题？老百姓都愿意过安定的生活，在某种程度上说，群众的眼睛是雪亮的。 <br />
<br />
&nbsp; &nbsp; 最后，把群体性事件说成是少数人教唆“不明真相”群众引发的，还有推卸自己的责任的嫌疑。近些年大规模群体性事件的发生，往往有地方政府工作没有做好，发生问题后处置不当的原因。中办、国办日前印发的《关于实行党政领导干部问责的暂行规定》明确提出，要对“在行政活动中滥用职权，强令、授意实施违法行政行为，或者不作为，引发群体性事件或者其他重大事件”的党政领导干部进行问责。借口群体性事件是少数别有用心的人挑起，可掩盖自己工作失误或试图减轻自己应负的责任。 <br />
<br />
&nbsp; &nbsp; 当群体性事件发生时，轻易地称成百上千的群众为“不明真相”，是传统思维模式在作祟。贵州省委书记石宗源在就瓮安事件的经验教训回答记者提问时说，要打破群体性事件是“不明真相的群众在少数坏人的煽动下”发生的公式，值得人们深思。</div>

							
							
							
							
