													<h2>新华社终于讲出“不明真相”的真义</h2>
						
						
																											<div id="postmessage_182580" class="t_msgfont">航亿苇放话<br />
<br />
<br />
每有群体性事件，一些政府机关的文宣必定是“不明真相”的群众是被“别有用心”的人蛊惑的。可这套说辞却是最苍白也没有说服力的。新华社记者针对这一现象，写了《群体性事件中少用“不明真相”》一文，总算捅破了有关“真相”的那层窗户纸。一些媒体显著地转载此文，也有心照不宣之意。很多年来，某些政府官员在出了一些事之后，总是把着重点放在“别有用心”的人和“不明真相”的群众这一块。结果，真正事件起因、应当担责的官员便马虎搪塞过去了。<br />
<br />
在这世界，有些事的因果关系是明显的。比如，先有了贪污腐败、强拆房子，那才会有人聚众成“群体性事件”。在事件中，可能当真会有一些人不明真相，也有些人“别有用心”，但如果没有前因，“别有用心”的人再怎么制造乱子也不会有多少人相信。<br />
<br />
不去追究前因，而在事过后找几个“别有用心”或“带头”的人论罪，是南北极磁跳，方向乱了。A姑娘被PP先生强奸在先，PP先生又继续伤害她及亲友。某天，A姑娘的男友K生终于忍无可忍，打死了PP先生。这样的事，如果不管PP先生曾经的犯罪而仅仅处罚K生，那么，民众就倍感不公平。<br />
<br />
邓玉娇案中，玉娇被邓贵大等侵害在先，她杀死邓贵大在后。因果关系明显，很多认为玉娇是正当防卫，包括一些律师都这么认为。有关方面处理邓玉娇案时，一味在玉娇的“罪”上做文章而对邓贵大等人的罪轻描淡写，有故意为权贵隐瞒之嫌，这才使邓玉娇案被放大，为全国舆论特别关注。<br />
<br />
某些群体性事件直接起因看上去有些荒唐，或许当真是谣言所致，但其深层原因，并不是一点谣言所能决定的。那必须从一些历史过程来看。很多官员最起码学过一些历史。历史经验早就说了，社会矛盾是一个长期酝酿的过程。谣言，有时不过是导火索而已。一个炸弹已经埋在那儿，它其实随时会爆炸。比如某企业，总经理一年工资300万元，工人月工资只有三四百元。这不仅是差距太大，而是三四百元的工资让人很难活下去。这样，一颗炸弹就埋在那儿了。某天，总经理可能会因为一个眼神激怒工人，从而引发所谓群体性事件。但事件的真相绝不是因为那个眼神。<br />
<br />
有些群体性事件根本不是什么群众不明真相，而是当地某些官员恶劣行为的积累刚好与这件事关联越来了。而这，也才是很多群体性事件真正的真相。这些真正的真相，群众亲身体验了多年甚至几十年。他们不是“不明真相”，而是太了解真相了。而有些政府官员最害怕某些群体性事件背后的大真相曝光，就将群体性事件乱定性。说某事是“别有用心”挑唆“不明真相”的群众引起的，某些贪污腐败、粗暴无礼、浪费严重的官员的责任甚至罪行就被掩盖掉了。<br />
<br />
现在的社会寻找真相很难。很多事，是有一批人特别难知道真相。那是上级领导和执法反贪等机构的人员。比如某贪官，城内很多人知道他拥几个老婆，有多处明显有嫌疑的房产，但纪检、反贪局就是“不知道”这回事。近两年几件“大事”，不正是群众帮助有关部门弄清了真相吗？如“躲猫猫”、“俯卧撑”、“高价烟局长”等。<br />
<br />
新华社记者终于说出了大实话：说群众“不明真相”，并不等于真理站在了自己一边，反而暴露有关部门没有尽到应尽职责。确实如此。如果在一方水土生产出那么多“不明真相”的群众，那么，政府部门干什么吃掉了？你不把真相通过有效途径告诉群众，然后反过来责怪群众“不明真相”，这究竟是什么逻辑？<br />
<br />
每有群体性事件，政府的态度很重要。如果及时从自身找原因。该道歉的道歉，该说明的说明，该补救的补救，一些群体性事件就能顺利解决。但如果不是这样，政府没有反省和自我纠错能力，那么，一次群体性事件反而可能变成下次更大规模更严重群体性事件的诱因！如果这样的恶行循环持续下去，会最终发生什么事，恐怕很多人眨眼间就想透了。<br />
<br />
各级政府及官员，当真不要再自欺欺人地用套话、官话欺骗别人也骗自己了。一些老得掉渣的说法，连小蚂蚁都不信了，一些官员还以为尚有人信。而这，正是中国特色的一种悲哀吧。<br />
<br />
补充：“群体性事件”这词也太拗口了，虽然不得不用，但就是无法习惯这样的词汇。<br />
<br />
