													<h2>是谁令群众“不明真相”的</h2>
						
						
																											<div id="postmessage_183785" class="t_msgfont">贾葭 <br />
<br />
　　<br />
<br />
　　7月28日晚，新华社刊发题为《群体性事件中少用“不明真相”》的时评，其中指出：“一些地方在发布有关群体性事件的信息时，爱用‘不明真相人员’‘不明真相群众’等词句。……这一说法耐人寻味，值得反思”。这两天来，各大新闻网站和平面媒体竞相转载此文，大部分读者对此文高度赞扬。<br />
<br />
　　过去十数年来，“不明真相”之于民众而言，不是一个陌生的词汇。它经常出现在群体性事件过后的官方解释或见诸媒体的新闻通稿中，通常的表述是“不明真相的围观群众”。一般在发生群众大规模聚集的事件后，一些地方政府往往会不假思索地祭出这一说法，且使用一分为二划分阵营的方法，指大部分不明真相的围观群众受“一小撮别有用心的人”蛊惑与挑唆。<br />
<br />
　　真相可能是这个世界上最不易接近的东西。可以说，在经验所及的范围里，我们对大部分事情的真相都不了解，等权威信息发布，大家才恍然大悟。从这个意义上说，不明真相是一种常态。比如季羡林先生的遗产风波，真相至今不为人知。比如余秋雨老师能早于中纪委察觉某前地方领导的不轨，那简直是一种特异功能，不具代表性。通常而言，和普通群众距离比较远、关系也不那么密切的事件，不明真相很正常，也可以理解。<br />
<br />
　　近年来的群体性事件中，“不明真相”的发生逻辑却恰恰相反。许多事件的发生，老百姓是身在现场亲自参与的。比如最近的湖北石首事件，表面看上去是因一非正常死亡人士的家属保护其亲人的遗体所引发，实则当地百姓对当地政府早已怨声载道，便借围观这种方式发泄不满。一些地方因征地补偿纠纷引发群体性事件，地方政府也常称事件在场人员为“不明真相的围观群众”，于情于理都不妥。补偿款是多是少，群众心里有数得很，实在不能称作“不明真相”。<br />
<br />
　　一些地方政府似乎特别钟情于这个词汇。大事小事发生，一概称之为“不明真相的围观群众聚集”，好像中国老百姓特别喜欢看热闹。退一万步说，看热闹如果没有造成公共秩序混乱，并无不可。天安门每天早晨升旗的时候，看升旗的人，也可以称为“围观群众”。但一些领导似乎特别恐惧于群众聚集，哪怕只是看看热闹也不行。给外界的感觉是，他们似乎从“围观群众”那里很是感到某种不安。<br />
<br />
　　为平息这种不安与恐惧感，围观的群众必须要在短时间内被解散或者强行驱散，不论这种围观多么秩序井然多么安静平和。这就很容易形成一个恶性循环：群众自己认为看到的是真相，而政府却偏说不是，且硬塞给他们另一个真相――你们是受一小撮别有用心的人挑唆的，赶紧回家洗洗睡吧――长此以往，稍微有点智商的人恐怕都不能容忍这种侮辱吧。<br />
<br />
　　这种表述模式如今已经走到尽头。作为一位媒体人，其实我也经常自嘲，自己才是且长期是“不明真相的围观群众”。假使某地发生一起群体性事件，记者与媒体远离现场，连围观都毫无可能，是不能随便指围观群众不明真相的。即便到达现场，但挤不进围观群众的前三排，也不能声称自己就掌握了真相。<br />
<br />
　　在早年信息沟通不畅的时代，某件事发生后，事件发生地官方解释是唯一的声音，也是唯一的“真相”。若记者（是且只是新华社记者）不到现场，所有发生的事情都不能算是新闻。若记者到现场之后，事情并未报道出来，也几乎等于这件事情从未发生。一些地方领导可以在小范围内焦头烂额地开会讨论对策，但就是不对公众说一丁点――那时候，“真相”是被他们掌握的。<br />
<br />
　　传媒的市场化改革以及网络时代的到来，终结了这种只有一种声音的时代。这个时代的特征是，真相不是一些闭目塞听的领导的禁脔。知道真相是群众的权利，哪怕他们只是在边上围观，也有权利知道真相。而网络、手机短信，则为他们寻求真相提供了工具便利。事件发生后，第一时刻全国全世界都能看见，不借助官方媒体，真相照样可以传播。这是市场经济给中国带来的巨大变化之一。<br />
<br />
　　<strong>以前我们也被告知，“群众的眼睛是雪亮的”，后来就变成“不明真相”了。或者可以这样说，群众的眼睛视事件性质的不同，时而不明真相，时而眼睛雪亮。总之，群众判断力不是由群众自己决定，而是由某些领导决定，归根到底这还是皇权时代“牧民”的心理。政府治理的现代化，应从让群众知道真相开始。</strong><br />
<br />
　　（作者系资深媒体人）</div>

							
							
							
							
