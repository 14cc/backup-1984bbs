													<h2>六・四当日，我的经历</h2>
						
						
																											<div id="postmessage_113959" class="t_msgfont">中文摘要：<br />
六月四日 在广场上带了个黑箍（吊丧用的）走来走去<br />
然后他们就把我拉到警车里去了 <br />
盘问了一会儿 <br />
我没能像【十七只猫】那个人一样坚强<br />
&quot;我家里有人，二舅，李伯光，56，死了&quot; “单位？”“不知道” <br />
看我未成年+女孩+有效护照（他们根本不会用……） 把我放走了 <br />
全过程被他们录像<br />
<br />
<br />
<br />
<br />
英文详述<br />
Interrogation by Police<br />
<br />
Today I had this black thing on my shoulder which in Chinese tradition means that somebody close to you has died and you want to commemorate them. There was no trouble during the transportation to the Square. Arrived at 9:10<br />
Until after 25 minutes of standing if front of the Mao face of the Heaven Gate, this fat bald policeman took me by the hand while I was taking a picture. This time I really understood the meaning of things “coming out of nowhere.”<br />
He asked, “Do you have ID?” I said yes, and gave him my passport. “What country is this?” “Can’t you tell by the cover?” “What country is this?” “Look on the cover.” “What country is this?” “America.” “I see.” He had no clue how to obtain information from that passport.<br />
He asked why I was wearing this, I said because of a family member died. “Who?” “Second uncle on the mother’s side（二舅）” “Hm. What’s his name?” “李伯光(I had no preparation AT ALL until he asked this, and I always thought I sucked at lying.)”“How old is he?”“56 (another spontaneous one…).” Two policemen were constantly filming the whole process, as well as documenting my passport. Another policeman with a uniform asked if he should go check the files. The bald man ignored him and continued to ask me, <br />
“which firm does he work for?””I don’t know.” Then he took off, and I followed because my passport was still with him. He stopped at a van and asked me to go on the van. I asked “where are we going?” “Nowhere. Just better to talk inside.” I acted hesitant and timid intentionally (my friend advised me, ‘cry when in trouble!’), but I was truly a bit worried as well. <br />
On the van, I clenched to my bag but they made me leave it on another seat while I sat down on one. There were about 8 policemen on the car. Most of the interrogation in the van was about where I graduated, whom I lived with, etc. etc. And they had no clue how to check my passport, which I had to explain. After a while, they let me go, “You have to understand, this is our job. We understand you too, wanting to express your grief for your family, but please do not wear that thing today on the Square.”…………BUT I SWEAR THEY KNEW I WAS BS-ING!<br />
<br />
While on the van, they were walkie-talking to each other about politically suspicious people, “THIS JAP REPORTER REFUSES TO REGISTER AND LEAVE!”… “JAP REPORTER HEADING TOWARDS WEST OF SQUARE! ROGER”……like that.<br />
<br />
Then I took some pictures of the rainbow under-covers, left at 11:30.<br />
<br />
And my blog-friend once said, “This year’s anniversary period will result in the government's over-estimation on the activism of college students these days. ”<br />
