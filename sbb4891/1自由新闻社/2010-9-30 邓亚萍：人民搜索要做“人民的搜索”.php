													<h2>邓亚萍：人民搜索要做“人民的搜索”</h2>
						
						
																											<div id="postmessage_849973" class="t_msgfont"><a href="http://tech.163.com/10/0930/04/6HQ6PCVK000915BF.html" target="_blank">http://tech.163.com/10/0930/04/6HQ6PCVK000915BF.html</a><br />
<br />
2010-09-30 04:19:40　来源: 京华时报(北京)　跟贴 0 条 手机看新闻&nbsp;&nbsp;<br />
<br />
“乒乓女皇”、剑桥经济学博士、国际奥委会官员、共青团北京市委副书记、人民搜索总经理，邓亚萍每一次迅速而利落的转身，都让人想起当年球桌前“小个子巨人”利落的抽球。<br />
<br />
昨天，邓亚萍接受了本报记者的专访。她表示，人民搜索要做“人民的搜索”，真正服务于大众。<br />
<br />
人民搜索会真正服务于大众<br />
<br />
做好新的工作需要很多人的帮助，需要整个团队的努力，更需要我们广大网民的支持。<br />
<br />
京华时报：出任人民搜索网络股份公司总经理以来，公众对您更加关注了。对于新角色您有怎样的展望？<br />
<br />
邓亚萍：我非常感谢大家对我的关注、关心。我也注意到近期大家对我出任人民搜索总经理一事展开热议，我认为这源于大家对我的喜爱和关心。<br />
<br />
这种关切将会更加激励我去把新的工作做好。当然做好眼前的新工作，需要很多人的帮助，需要整个团队的努力，更需要我们广大网民的支持，与我们一同建立人民搜索。<br />
<br />
我非常希望得到大家真诚的建议和意见，将人民搜索变为“人民的搜索”，真正服务于大众。<br />
<br />
京华时报：在出任人民搜索总经理之前，您对网络有什么初步印象？<br />
<br />
邓亚萍：对于互联网，我认为这取决于自己想在网络上得到什么。每个人的时间非常有限，你可以在网上花费很多时间，但也可以通过互联网便捷地获取更多知识。这答案就像很多人问过我“成功的秘诀是什么”，秘诀就是“在有限的时间里做最有价值的事情”。<br />
<br />
在新领域牢记自己是名学生<br />
<br />
在这个领域里我是一个新手，一定要向业内成功人士学习，有大家的支持，我相信能把这个事情做好。<br />
<br />
京华时报：您从体育界到政界再到互联网行业，短时间的大跨度转身，让外界感叹不已。您个人怎么看待这种跨越？<br />
<br />
邓亚萍：我从事的这些工作，看起来确实跨度很大。从运动员到国际奥委会，在国际奥委会期间经历两次申办奥运会过程，又经历了北京奥运会筹备阶段，之后到了共青团，如今又到现在的单位。但实际上，我工作和服务的对象始终有一个不变的人群，那就是我们的年轻人。<br />
<br />
从运动员的经历开始，从业者就是年轻人。到了奥组委更是这样，无论组委会工作人员还是志愿者，绝大多数都是年轻人。那个时候大家普遍认为“80后”是“垮掉的一代”。但是从汶川大地震到北京奥运会，我们的“80后”通过自己的能力征服了世界。因此，奥运会过后，他们有了一个新的名称叫“鸟巢一代”。<br />
<br />
京华时报：除了这些相通的特点，有没有不同之处，对自己有何挑战？<br />
<br />
邓亚萍：每个人在成长中都会经历无数次的挑战，面临无数次的新生活、新学习、新工作的环境，包括新的同事。对于我们来讲，每经历一次，就是人生阅历的积累，更重要的是，每经历一次就让我学到更多的这个领域的知识。<br />
<br />
京华时报：怎样调整自己在短时间内适应大跨度的转身？<br />
<br />
邓亚萍：人生下来没有什么都会的，我们的第一任老师是我们的父母；在成长的过程中，老师、同学给我们帮助；走到社会上，同事、领导和朋友们给我们指导。<br />
<br />
其实最简单的一个道理就是，在一个新的领域，自然有比你懂得多的人，自然有比你有经验的人，自然有专家、学者、业内精英人士，只要虚心向他们学习，就能在最短的时间内了解和掌握这个领域的知识。<br />
<br />
比如一个年轻的乒乓球运动员来请教我，我会毫无保留地把经验传授给年轻人。而我现在是一个学生，在这个领域里面是一个新手。我一定要向业内成功人士学习，也希望他们能够给我一些他们经历中最宝贵的财富和秘诀。只要有大家的支持，我相信我们能一起把这个事情做好。<br />
<br />
会以谦卑的态度虚心学习<br />
<br />
去读一个剑桥的博士，只是证明我还会读书。我会虚心向专家学习，相信他们会不吝赐教。<br />
<br />
京华时报：从体育明星到剑桥博士、再到官员，在这么多成功光环笼罩下，还能做回学生的平常心吗？<br />
<br />
邓亚萍：中国古话说得好，人活到老学到老。古人都如此，更不要说我。<br />
<br />
去读一个剑桥的博士，不等于我什么都懂、什么都会，只是证明我还会读书，我还能读书，仅此而已。并不等于说，拿到一个剑桥的博士就包打天下。我会以谦卑的态度向专家虚心学习，我也相信他们会毫不吝啬地传授给我。<br />
<br />
京华时报：社会公认您是体育明星转型最成功的典型例子，你如何看待这种评价？<br />
<br />
邓亚萍：我和大家没有什么不同。你有你成功的地方，我也有我成功的地方，你们有你们的特长，我也有我自己的特长。我就是我，我就是一个邓亚萍。我从来没有觉得我高于谁、特别于谁，或者说我有什么特殊的功能。我能够到今天是大家鼓励、支持爱护的结果。面对新的挑战，我想得到大家的支持和帮助。<br />
<br />
成功者有强大的内心世界<br />
<br />
刚入清华时，我认为自己是清华最差的学生，但后来，我找到了自信的理由。<br />
<br />
京华时报：“我就是我”让人感到您有一个强大的内心世界，这是成功的因素吗？<br />
<br />
邓亚萍：我举例来讲。就像我去清华读书一样，当时我就想，我怎么能在清华立足？大多数同学读书的时期我打球去了，所以别的同学的知识储备，我怎么能比得上呢？刚入清华时，我认为自己是清华最差的学生，平心来说的确也是。<br />
<br />
但后来，我找到了自信的理由。在我们这么多同学中，我相信没有一个人有我这样的经历，那就是通过自己的努力和大家的帮助，夺取一个又一个冠军，为国争光。而且作为运动员，我走南闯北，历经多次决战，什么场合没见过，什么压力没承受过？<br />
<br />
既然同学们有他们的优势，我也有自己的特长。我是来弥补我知识上的缺陷，想继续完善我自己。我进清华时24岁，那时很多同龄人已经大学毕业了，但我坚信任何事情从现在做起都不晚。<br />
<br />
就是凭着这么一点自信，开始学习，开始用功，也走过来了。很难吗？有奥秘吗？其实最重要的就是自己的心态。</div>

							
							
							
							
