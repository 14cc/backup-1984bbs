													<h2>重庆被洪水围城 地标朝天门被淹(组图)</h2>
						
						
																											<div id="postmessage_193848" class="t_msgfont">来源：中新社重庆 八月六日报道<br />
　　<br />
　　 记者六日从重庆防汛抗旱部门获悉，五年来长江中上游最大一次洪峰六日凌晨通过长江重庆寸滩段。另据重庆市民政局消息称，连日来持续强降雨已致重庆二十三个区县一百五十四万人受灾，因灾死亡十人，失踪一人。<br />
　　<br />
　　受长江上游和本地强降雨影响，重庆主要江河水位也普遍上涨，长江寸滩段和嘉陵江等五条中小河流均出现超警戒或保证水位洪水。据当地水文部门监测，六日凌晨二时，长江寸滩站洪峰水位达到一百八十三点一一米，相应流量五万六千七百立方米每秒，超警戒水位二点六一米。这是二00四年以来长江中上游出现的最大一次洪水过程。<br />
　　<br />
　　据统计，二日二十时至五日七时，重庆二十三个区县遭受持续强降雨，其中，二十个乡镇强降水雨量累计超过三百毫米，八十六个乡镇雨量累计超过二百毫米，一百九十一个乡镇雨量累计超过一百毫米。<br />
　　<br />
　　这场自西向东的强降雨天气过程，导致重庆部分地区引发滑坡泥石流灾害。截至五日十七时，持续强降雨已致重庆一百五十四万人受灾，紧急转移安置十二点三万余人，因灾死亡十人，失踪一人；农作物受灾面积六十三点六千公顷，绝收面积达四点四千公顷，万余间房屋倒塌，一万六千余间房屋损坏，当地因灾直接经济损失六亿八千万元人民币。其中，重庆铜梁、潼南、大足、北碚等区县受灾情况相对较重。<br />
　　<br />
　　针对重庆暴雨洪涝灾害，中国国家减灾委、民政部于五日十七时紧急启动国家四级救灾应急响应，并派出工作组赶赴重庆，协助当地指导救灾工作。中共中央政治局委员、重庆市委书记薄熙来就洪灾救灾工作作出批示，重庆市民政局派出的六个工作组已分赴重灾区核查灾情，五百床空调被也已紧急调运灾区，各项抗灾救灾工作正有序进行。<br />
　　<br />
　　(本文来源：中国新闻网 作者：杜远) <br />
