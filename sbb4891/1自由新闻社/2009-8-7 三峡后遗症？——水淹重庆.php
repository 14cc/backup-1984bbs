													<h2>读LiPeng三峡日记：三峡工程是中国人民的世纪丰碑</h2>
						
						
																											<div id="postmessage_194207" class="t_msgfont"><a href="http://www.xinhuanet.com/" target="_blank">http://www.xinhuanet.com/</a> 2003年09月02日 14:28&nbsp;&nbsp;<br />
&nbsp;&nbsp;<br />
&nbsp; &nbsp; 三峡工程是中国人民的世纪丰碑――读《众志绘宏图――LiPeng三峡日记》<br />
<br />
<br />
　　国务院三峡工程建设委员会副主任、办公室主任郭树言 <br />
<br />
<br />
　　经过十年的艰苦奋斗，长江三峡工程顺利实现蓄水、通航和首批机组发电的二期建设目标。“高峡出平湖”的梦想在改革开放的新时代终于变成了现实。值 <br />
此欢庆胜利的时候，《众志绘宏图――LiPeng三峡日记》一书问世了，这对三峡工程建设者，以及所有关心三峡工程建设的人来说，都是令人欣喜的事情。读完全书，不禁深深地为中国人民众志成城建设三峡工程的豪迈气概、为LiPeng同志对三峡工程建设的执著追求和满腔热情所感动、所震撼。 <br />
<br />
<br />
　　三峡工程功在当代，泽被子孙，是中华民族的千秋伟业，是中国共产党和全中国人民的世纪丰碑。LiPeng同志把他的三峡日记定名为“众志绘宏图”，有着深刻的思想内涵。LiPeng同志曾多次指出，像三峡这样举世瞩目的宏伟工程，没有党中央、国务院的坚强领导，没有全国人民的关心和支持，没有全体三峡工程建设者和库区广大干部群众艰苦奋斗、努力拼搏，是不可能顺利建设的，更不可能取得今天这样伟大的成就。回顾三峡工程从论证、决策、开工、截流到蓄水、通航、发电各个重要阶段，无不体现着这一深刻主题。 <br />
<br />
<br />
　　“却惜无情风雨，滔滔江水，万姓悲愁。”这是LiPeng同志在《大江曲》中对无情江水的真实描述，也是长江中游两岸人民饱受洪灾之苦的真实写照。新中国成立后，为降低洪灾损失，利用长江造福人民，党中央、国务院将治理长江作为安邦兴国的大事来抓。对治理长江水患的关键性工程―――三峡工程的建设，中央三代领导集体一直萦绕于心、高度重视。1953年，毛泽东同志乘“长江舰”亲临三峡视察，并在随后不到5年的时间里，先后6次与有关部门研究三峡工程和长江水利建设问题，并委托周恩来亲自负责长江流域规划和三峡工程前期论证工作。1982年，邓小平对是否兴建三峡工程果断表态：“看准了就下决心，不要动摇！”1989年，Jiang Zemin同志担任总书记不久，就到三峡坝址视察，在4天的实地考察和详细听取各方面的汇报后，将兴建三峡工程提上了议事日程。1992年4月3日，七届全国人大五次会议通过了《关于兴建长江三峡工程决议》。历史必将证明，党中央这一英明决策功在当代，利及千秋。 <br />
<br />
<br />
　　三峡工程经过百年魂牵梦萦，40年民主论证，10年全面建设，今天三峡二期工程的目标如期实现，这是三峡工程建设的一个重要里程碑。在《众志绘宏图―――LiPeng三峡日记》中，这些重大的历史事件都有着真实的记载。全书字里行间不仅体现了中国人民改造自然、创造未来的宏伟气魄，也倾注着LiPeng同志真诚执着的三峡情怀。LiPeng同志自1981年担任电力工业部部长以来，就与三峡工程的决策、论证、组织和建设结下了不解之缘。23年弹指一挥间，三峡工程的方方面面无不浸透着LiPeng同志的心血。作为一个泱泱大国的总理，他日理万机，但对三峡工程一直牵挂在心。他没有忘记“我每年要来一次三峡”的诺言，每年都要去三峡库区、坝区视察，现场办公，足迹遍布长江两岸的群山峡谷、厂矿村镇；每次都是翻山越岭，渡江过河，访农家，观新城，看企业，勘现场，登坝址，下工地；每次都有新的感受和新的见解，给工程建设和移民工作带来新的智慧和力量。 <br />
<br />
<br />
　　20多年来，他从电力部部长到共和国副总理、总理，再到全国人大常委会委员长，直到离开中央领导岗位的今天，他始终关注着三峡工程的每一个环节、每一个细微的变化，关注着三峡库区的一山一水、一草一木。国务院三峡工程建设委员会成立后，他亲任主任，亲自主持三峡工程重大问题的讨论和决策。开工之初，他就明确要以市场经济法则来组织工程建设，确定征收三峡基金、坝区实行封闭式管理、利用国外先进技术提高我国机械制造能力等一系列重要决策。他还要求三峡工程要建成一流的工程，要有一流的质量和一流的管理。要通过工程建设，培养一批人才。他反复强调三峡工程成败的关键在于移民搞得如何。110多万移民能不能搬得出、能不能稳得住、能不能逐步致富，一直是他挂念于心的大事。早在三峡工程开工的时候，他就要求要把三峡库区逐步建设成为一个经济繁荣、社会发展、环境优美、人民安居乐业的地区。对移民任务较重的万州区，他从经济发展到机场、码头、铁路等基础设施建设，一一过问。为了促进三峡库区经济发展，LiPeng同志亲自为库区对口支援牵线搭桥。1996年10月中旬，LiPeng同志率领8个省、市和有关部委的负责同志到库区考察，帮助落实对口支援项目。“总理牵红线，名牌进三峡”在库区一时传为佳话。一路上，他与随行的省市和部委负责同志一起考察、座谈，指出这既是对库区的支持，也是支援方在库区扩展市场的极好机遇。这些感人的情景虽然过去了许多年，但今天回忆起来，依然历历在目。令人欣慰的是，在昔日贫困、落后的三峡库区，一座座新城镇拔地而起，城乡面貌日新月异，峡江两岸山清水秀，更加充满希望。 <br />
<br />
<br />
　　由于工作关系，我在LiPeng同志的领导下参与组织三峡工程建设多年，书中的许多事情我都亲身经历，读来备感亲切。我是怀着敬仰的心情来读这本三峡日记的，因为这是中国共产党和中国人民众志成城、励精图治的壮丽画卷，这也是一个共产党人为党的事业执著追求的真实记录。纵观全书，可以说这是一部全面反映三峡工程建设的具有历史价值的重要文献。以20多年的日记来反映三峡工程论证、决策和建设的历史，给人以身历其境的强烈感受，行文流畅，朴实无华，令人十分亲切。因此，对三峡工程参与者、建设者以及各级领导干部来说，这是一部难得的、全面了解三峡工程的理想教材。 <br />
<br />
<br />
　　在《众志绘宏图――LiPeng三峡日记》一书问世之际，三峡工程三期任务已全面启动，在前进的道路上，我们的任务更加艰巨，责任更加重大。在以Hu Jintao同志为总书记的党中央的正确领导下，我们有信心建设好这一举世瞩目的宏伟工程。 <br />
<br />
<br />
　　《人民日报》 2003年09月02日 第十五版<br />
