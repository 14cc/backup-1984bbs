<br />
网络上炒得沸沸扬扬的“宝马女”马诺说过一句经典的话：“我更愿意坐在宝马车里哭。”她以此为由拒绝了爱骑自行车的男青年。<br />
<br />
慈溪有一位“宝马新娘”，却连8辆奥迪也拒绝了。昨天，论坛上一则名为《慈溪8辆奥迪，接不了新娘？说非宝马不上车！》的帖子吸引了许多网友的关注。<br />
<br />
<strong>网友“大宝宝”讲了一桩“牛事”</strong><br />
<br />
5月30日隔壁家新娘结婚，新郎借了8辆奥迪来女方家接人，主婚车也是奥迪。到了女方家门口，新娘一看都是奥迪，就对新郎说：“怎么都是奥迪啊，连主婚车怎么也是奥迪啊。”新郎说车子借不到了，新娘就开始耍脾气说不够好，起码要宝马740来接才上车。新郎说了好多好话，劝了好多，新娘就是不上车。<br />
<br />
接着小舅子们看不下去了，就对新娘说：“今天的日子算了，上车吧，男方家还等着我们过去呢。”新娘还是不上车。小舅子们又说：“那我们先上车了哦。”没想到新娘甩出一句话：“你们上车，就是丢我们家的脸。”这一句话把那些小舅子都气晕了，于是有车的就开车回家，没车的就打的回家。尴尬不已的新郎指着新娘的鼻子甩出一句话：“算你狠，我算看清楚你了。”接着空车回去了。<br />
<br />
好好的一场喜事，结果变成这样。我们都觉得新娘实在过分啊，这样的举动，让两家人怎么再抬得起头啊。<br />
