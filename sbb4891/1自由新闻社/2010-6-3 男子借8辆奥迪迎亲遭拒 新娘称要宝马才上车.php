<strong>日子和面子</strong><br />
<br />
中国式的结婚，大多数是围绕着房子、礼金、陪嫁、酒席这四件事而相伴相生。古话说：礼轻情义重，在现实中还有多少人认同？什么样的礼金(陪嫁)标准才能称之为拿得出手？也许没有标准答案，说到底还是心态，既是在考验男女双方的心态，也是在考验双方父母的心态。<br />
<br />
正在准备结婚和刚刚结婚的人们，调节好自己的心态。结婚到底为了什么？是因为物质还是因为在你身边陪你度过每一天的那个人？这些因为房子、礼金、陪嫁、酒席而引发的矛盾，在我们的生活中短时间不会消失，关键是看身在其中的人怎么去处理。是化解，还是对抗？这是一道永远的选择题。（来源：现代金报）</div>

							
							
							
							
