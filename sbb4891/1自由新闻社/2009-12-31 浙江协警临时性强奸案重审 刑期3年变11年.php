浙江协警&quot;临时性强奸&quot;案重审 刑期3年变11年<br />
2009-12-31 14:42:45　来源: 中国新闻网(北京)　跟贴 1099 条 手机看新闻<br />
核心提示：曾引发“临时性”这一网络名词的“南浔强奸案”，经过浙江省湖州市中级人民法院重新开庭审理后，今天作出一审判决，以强奸罪判处被告人邱某某有期徒刑11年，剥夺政治权利1年；判处被告人蔡某某有期徒刑11年6个月，剥夺政治权利1年。<br />
中新网12月31日报道 曾引发“临时性”这一网络名词的“南浔强奸案”，经过浙江省湖州市中级人民法院重新开庭审理后，今天作出一审判决，以强奸罪判处被告人邱某某有期徒刑十一年，剥夺政治权利一年；判处被告人蔡某某有期徒刑十一年六个月，剥夺政治权利一年。<br />
<br />
据了解，今年10月29日，浙江湖州市南浔区法院对两名派出所协警强奸醉酒女子一案进行开庭审理。审理调查后南浔法院根据犯罪事实，考虑到两被告人属临时性的即意犯罪，事前并无商谋，且事后主动自首，并取得被害人谅解，给予酌情从轻处罚，最终判决两被告人各入狱三年。<br />
<br />
随后该案被媒体在网络上进行了报道，但立即引发网友对法院的“临时性”一词和该判决的公平公正性提出质疑。一时间，“临时性”在网络上被炒得沸沸扬扬，引起关注。<br />
<br />
2009年11月6日，湖州市中级法院对被告人邱某某、蔡某某强奸一案作出再审决定。法院审理查明，被告人邱某某、蔡某某是南浔区善琏镇综治协会聘用的保安，驻善琏派出所协助维护社会治安工作。2009年6月10日，两被告人邀请女青年陈某、沈某某(两人分别于2008年职业中等技术学校毕业、私立高中辍学)等人吃晚饭。饭后两被告人乘陈某、沈某某酒醉之机，在宾馆房间内先后分别对两被害人实施奸淫。<br />
<br />
当晚，沈某某之父得知其女儿可能被强奸，即向公安机关报案。次日上午，善琏派出所领导了解到两被告人有重大作案嫌疑，遂通知两人回所谈话，经教育两被告人逐步交待了作案经过。法院认定被告人邱某某、蔡某某的行为均已构成强奸罪，且属轮奸。两被告人虽能如实交代自己的犯罪事实，但不属自动投案，不能认定为自首。<br />
<br />
经审理，裁定撤销南浔区法院原判决，发回重新审判，后经检察机关撤回起诉并退回公安机关补充侦查。侦查终结后，南浔区检察院认为案情重大、复杂，将案件报送湖州市检察院，湖州市检察院将该案向湖州市中级法院提起公诉，湖州市中级法院经不公开开庭审理，并作出上述判决。<br />
<br />
另据获悉，由湖州市纪委、市委政法委组成的联合调查组经过认真调查认定，未发现原案相关办案人员在案件侦查、起诉和审判过程中有接受吃请、收受贿赂、徇私枉法的行为，但存在业务素质不高、作风不扎实以及办案程序不够规范等问题。当地有关部门对该案原办理过程中存在失职行为和管理不力的责任人员，依照相关规定进行了严肃处理，已免去南浔区公安分局善琏派出所副所长陈某某的副所长职务，并给予行政警告处分；给予南浔区检察院主诉检察官郑某某、南浔区法院主审法官郭某某行政警告处分，并均调离原工作岗位；给予南浔区法院副院长宣某某行政记过处分，并建议依照法定程序调离原工作岗位。<br />
<br />
另据记者了解到，此案发生后，当地政法机关以此为戒，举一反三，立即开展专项教育整顿活动。公安系统于10月份开展了为期一个半月的协辅警清理教育整顿活动，出台了协辅警管理规定；检察系统组织了以“严格执法、规范执法”为主题的案件质量检查和教育培训活动；法院系统开展了为期一个月的“大反思、大整改”活动。深刻剖析原因，认真进行整改，不断增强执法能力，改进执法作风，提高办案质量。<br />
