<br />
 本人系江南大学学生，一直以学校有没的环境和严格的管理自豪。但今日的“刚水门”让在下不吐不快。<br />
<br />
扬子晚报网消息 刚刚欢庆建校50周年的江南大学，新年伊始，迎来了“开门红”。在刚刚公布的由中国管理科学研究院武书连主持的《2009中国大学评价》中，江南大学排名升至第68位。江南大学系国家“211工程”大学，近年来的表现也在社会中引起了关注。<br />
<br />
但今日无锡新闻中爆出学校竟然用钢铁厂循环出来的水给学生洗澡，而且其中含大量重金属，铅含量超出国家标准十多倍。对此，学校后勤部的领导也闪烁其辞。<br />
<br />
大家注意下，我们在学校洗澡不是不给钱的。而且相当高，从2008年9月份起，地处无锡市的江南大学新校区实行一项“新政”，学生洗澡按2角/分钟刷卡收费，校方称此举是为了节约水资源。无锡自来水价格仍是民用2.40元/立方米，而12分钟学生怎么也用不了1吨水。<br />
<br />
 学校收的钱比自来水要高很多，但却给学生用钢铁厂出来的铅水。那我们大学四年真么洗出来要真有什么事学校该怎么说。我们不是要闹事，但我们要个说法。请大家注意，江南大学是一所学校，不是商业企业，学校不能没良心赚这种钱啊！！！<br />
