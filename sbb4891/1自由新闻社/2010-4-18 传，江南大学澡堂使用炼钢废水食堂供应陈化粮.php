<a href="http://www.tianya.cn/publicforum/content/no110/1/54721.shtml" target="_blank">http://www.tianya.cn/publicforum/content/no110/1/54721.shtml</a><br />
作者：Erinsue 提交日期：2010-4-17<br />
<br />
江大钢铁废水洗澡门风波刚起，我们又迎来了另一个惊喜求后勤集团给说法。<br />
<br />
食品学院的学生一时兴起，到学校食堂打饭回去实验室化验，结果发现学校食堂给学生的米饭用的是陈化粮。大家也许不知道陈化粮是什么东西，陈化粮是指长期（３年以上）储藏，其黄曲霉菌（目前发现的最强致癌物质，２８０摄氏度高温下仍可存活，试验表明，其致癌所需时间最短为２４周）超标，已不能直接作为口粮的粮食。国家规定，陈化粮只能通过拍卖的方式向特定的饲料加工和酿造企业定向销售，并严格按规定进行使用，倒卖、平价转让、擅自改变使用用途的行为都是违法行为。<br />
　　<br />
前几天刚刚被电视台曝光了洗澡水来自钢铁厂，且含铅量超标<br />
以下是学校给的解释，纯粹转载一下<br />
　　<br />
近日，有热心市民和媒体反映我校浴室专用热水供应商“旺盛热水”供应的浴室用热水铅指标不合格。接到反映后，学校高度重视，积极开展调查，并已邀请无锡市卫生局、市疾病预防控制中心、市卫生监督所对旺盛热水所供热水质量进行全面调查检测，检测结果将在第一时间向师生公布。<br />
<br />
学校一贯重视各类生活用水质量，开水房热水为学校自备的直燃式开水炉加热的自来水，师生可放心使用。旺盛热水所供热水仅供浴室使用，从2009年10月供应以来一直定期将热水水样送无锡市疾病预防控制中心检测，至目前为止，各批次检测结果均为合格。<br />
<br />
为维护学校和师生利益，从对师生身体健康负责的角度出发，经研究决定，在无锡市卫生部门新的检测结果出来之前，暂时停止开放浴室。<br />
<br />
由此带来不便，敬请广大师生谅解。<br />
