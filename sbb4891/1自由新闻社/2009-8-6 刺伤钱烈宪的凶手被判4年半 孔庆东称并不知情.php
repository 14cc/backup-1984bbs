<br />
　　据相关消息称，杨春正是北大教授孔庆东的助理。2008年11月14日，“钱烈宪”曾发表了一篇题为《传孔庆东被拘审查》的博客文章，称孔庆东涉朝鲜间谍案被带走调查。正是这条博文给“钱烈宪”招致了祸端。据悉，孔庆东对杨春此举可能并不知情。<br />
