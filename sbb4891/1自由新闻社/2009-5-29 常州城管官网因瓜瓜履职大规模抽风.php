													<h2>扬子晚报趣味报道：常州城管网站遭黑客恶搞</h2>
						
						
																											<div id="postmessage_106610" class="t_msgfont">　　首页被加上“城管队员武艺高强，菜农溃不成军”等小标题<br />
<br />
　　本报讯 这两天不少常州市民在打开常州市城市管理局网站时，惊讶地发现整个网站被恶搞得面目全非，网页上满是侮辱城管的言语和照片，想查找的信息都没有了。直到昨天中午9点多钟，网站才恢复正常。据了解，网站被黑后内容基本上没有被恶意删除，只是增加了一些恶作剧的内容。目前，常州警方已介入调查。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;5月29日晚，常州市民吴先生无意中在政府网站上打开该市城市管理局网站的时候，发现一些让他大跌眼镜的内容。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 在该网站的首页上，竟然有一条大标题写着：“城管队员按摩女 河蟹一家齐欢乐”。下面则是一排类似内容的小标题：“城管队员深入实践，集体视察洗头房”；“城管队员深入实践，集体殴打小贩”；“城管队员武艺高强，菜农溃不成军”等。但点击后 ，里面并没有其他恶意内容，有的则是一幅情色图片：一男子衣衫不整搂着两个浓妆的女子，周围栏目里充斥着和首页上大致一样的诽谤内容。网站上的内容先是让吴先生吓了一大跳，不过随后想到，“这个网站肯定是被黑了”。<br />
<br />
　　当晚，常州一些网站论坛的网友发现此事后开始发帖介绍有关城管局的网站被黑一事。一位网友在一帖子里贴出了被黑网站的截图。不过到晚上11点左右，网友又发帖说该网站已被关闭。但是，在昨天上午8点多钟的时候，常州市城管局的网站却又能打开，里面的内容则是被“黑”以后的页面 ，上面侮辱性的内容看上去触目惊心。<br />
<br />
　　记者注意到，打开首页，在一个名为“行政执法”子栏目里写着“：城管大战铁血战士和异性”、“啥是城管”等，点击打开后内容基本一致。而在“市容广告”的栏目中则出现：“今天我把一个卖茶叶蛋的老太婆打了”、“我把一个卖苹果的打成了半身不遂”等字样。网页最下面的“机关建设”栏目，下面的“行风建设”、“廉政建设”等子栏目的内容一律被“啥是城管”覆盖，点击打开后则是对城管的侮辱性照片。<br />
<br />
　　“我们看了感到很气愤！”对于网页被恶搞，常州市城市管理局办公室一位负责人表示，5月29日晚上发现情况后，随即与网站的维护公司方面取得联系，20分钟之内将网站进行了关闭。昨天上午，城管部门有关负责人向常州市警方报了案，目前警方正对此进行调查。常州市城管局这位人士说：“可能是有人想通过这种办法发泄不满吧，攻击者没有社会道德。”这位人士表示，目前城管部门已经与网站维护公司进行了沟通，将加强网站安全技术上的防范，做好预防工作。<br />
<br />
　　据了解，负责常州市城市管理局网站维护的是一家名为金马信息技术有限公司的it企业，该企业负责网站维护的工程师表示，他们也是不久前才得到网站被黑的消息。在得悉此事后，网络工程师于昨日开始对这一事件进行了调查，该工程师说，网站被黑不外乎两个原因，一是后台的系统管理密码泄露，另一个原因则是网站本身存在安全漏洞。该人士称，经过对服务器初步诊断，网站中的内容基本上没有被恶意删除，只是增加了一些恶作剧的内容。据该公司表示，目前他们已就此事报了警，一方面紧急修复页面，另一方面则加大防火墙防护能力。<br />
