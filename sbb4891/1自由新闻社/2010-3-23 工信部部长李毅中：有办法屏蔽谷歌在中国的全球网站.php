																											<div id="postmessage_527916" class="t_msgfont"><img src="http://i892.photobucket.com/albums/ac121/wodetuchuang/00-1.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
中国 更新时间 2010年 3月 22日 星期一 01:41 PM 格林威治标准时间<br />
<br />
<strong>谷歌中国冲突：官媒抨击谷歌价值观渗透</strong><br />
<br />
记者: 东方 | 华盛顿<br />
<br />
　　中国国家控制的媒体指责谷歌威胁要求退出中国是出于政治动机。海外媒体分析说，这表明中国政府对世界上最大的互联网搜索公司谷歌试图挑战其新闻检查制度的大胆做法越来越采取敌意的态度。与此同时，中国官方媒体刊登了中国广告商联名信，威胁说要对谷歌撤出中国带来的损失进行索赔。不过，纽约时报报导说，这封联名信可能涉嫌伪造。<br />
<br />
　　*中方日趋强硬*<br />
<br />
　　华尔街日报报导说，近几个月，世界最大的互联网企业之一的谷歌公司一直就它能否在不经过滤的情况下继续在中国运营搜索引擎的问题同中国官员进行谈判，不过，在公开场合，中国的部长们没有显示出任何妥协的兴趣。在最近召开的人大上，中国官员强硬地表示，谷歌必须继续遵守中国法律，不然就要面临后果。<br />
<br />
　　最近几天，中国的反应似乎趋于激烈。在刚刚过去的周末，中国官方的新华社发表一篇评论文章，用近乎冷战字眼对谷歌发动了猛烈的抨击。新华社的文章称“谷歌来华的真正目的似乎并非‘拓展商务’，而是充当了借助互联网输出思想、进行文化渗透、价值观渗透的工具”，谷歌“处心积虑地将美国的价值观和评判标准强加于中国”；谷歌试图“改变中国的社会制度”；建立美国的“思想霸权”“文化霸权”；谷歌退出中国“是美国政客们和一些政府机构‘跳上台面’，‘共同演出了一场闹剧’”。 <br />
<br />
　　人民日报旗下的环球时报也于近日发表社评，加入反谷歌“大合唱”。这篇题为《世界不欢迎白宫的谷歌》的社评说，谷歌已经成为美国政府“向外推行美国政治意愿及价值观最方便的工具”。环球日报还抨击谷歌公司为奥巴马总统竞选捐款，批评克林顿国务卿和谷歌CEO等人于本月初会晤，并表示“世界显然不欢迎由美国国务院操纵的谷歌”，称谷歌是“美国设下在思想上征服世界的陷阱”；互联网既然是自由的，那么谷歌就不应该追求思想帝国的权力。<br />
<br />
　　*法新社：凸显中国恼怒之情*<br />
<br />
　　法新社评论说，很显然，新华社这篇文章字里行间都清楚透露出北京政府对谷歌做法的恼怒之情。<br />
<br />
　　英文版的中国日报对谷歌批评中国政府监控和过滤互联网内容进行了反击，宣称“中国人民正享受着5000年历史中前所未有的自由”。中国日报的文章还反击谷歌指责中国政府审查互联网监视网民“不是出于无知，就是对中国的恶意攻击”。<br />
<br />
　　不过，观察人士说，中国政府最近判处在互联网上发表对民主宪政看法的前北京师范大学教师刘晓波11年徒刑以及判处为汶川地震中豆腐渣校舍工程遇难孩子家长维权的四川作家谭作人监禁的做法似乎无法印证中国人正在享受5000年从未有过的自由的说法。<br />
<br />
　<strong>*中国高官：有办法封谷歌全球网*</strong><br />
<br />
　　著名中国作家廖祖笙发表文章说，南方都市报报称，中国工信部部长李毅中对记者说，工信部正与谷歌进行磋商，以解决争端；工信部有权吊销谷歌中文网站的执照，并有办法屏蔽谷歌在中国的全球网站。<br />
<br />
　　廖祖笙在文章中说，这是以赤裸裸威胁的方式要全球最著名的搜索引擎协同党天下作恶。廖祖笙感叹道，一个享誉全球的搜索引擎尚且经受着要么屈服、要么走人的高压，更何况是命根子攥在党天下手中的升斗小民。他说，一党独大之下，国人对公权的蛮横并不陌生，也见识了党天下太多的“有权”和“有办法”。<br />
<br />
　　*纽约时报：中国官媒造假*<br />
<br />
　　与此同时，中国官方媒体还广泛报导了一封中国广告代理商的联署信，声称如果谷歌中国撤出，他们将要求获得赔偿。<br />
<br />
　　不过， 纽约时报日前报导说，这封所谓中国广告代理商寄给谷歌的信件疑为伪造，是一条假新闻。<br />
<br />
　　据纽约时报报导，被中国官方媒体所称的“不陪谷歌玩了”的27家广告代理商之一的深圳赢客网络公司(Shenzhen Winkee Networking)否认曾经签署过此封信件。<br />
<br />
　　深圳赢客网络的谷歌广告部销售代表冯明明(音)说：“我已经同公司高层及其他相关领导核查过此事，均证实，我们从未签署和发送过任何此类信件。”<br />
<br />
　　此外，彭博新闻中提到的22家公司中没有任何一家公司对信件的真实性进行确认。<br />
<br />
　　这条据信的假新闻至今仍然在中央电视台网站、人民网<a href="http://mnc.people.com.cn/GB/11166824.html" target="_blank">http://mnc.people.com.cn/GB/11166824.html</a>和新华网<a href="http://big5.xinhuanet.com/gate/big5/www.cq.xinhuanet.com/business/2010-03/19/content_19288194.htm" target="_blank">http://big5.xinhuanet.com/gate/b ... ontent_19288194.htm</a>等中国官方网站上广泛传播。<br />
<br />
　　纽约时报援引曾就职于哈佛大学伯克曼网络与社会中心的伊萨克.毛的话分析说：“把这么一大群独立的公司组成一个联盟，听起来令人难以置信。”<br />
<br />
　　谷歌在两个月前挑战中国的法律，声称即使不得不关闭公司在华网站，也要停止对搜索结果的审查。<br />
<br />
　　谷歌在中国的去留在本月底其中国网络服务运营的执照到期更新时将做出抉择。<br />
<br />
　　中国外交部发言人秦刚表示，谷歌公司如果撤出中国，将不会影响外国公司在华的整体投资环境。<br />
<br />
　　*华尔街日报：在华外企唇亡齿寒*<br />
<br />
　　不过，华尔街日报今天报导说，“谷中争端”给很多在华外资企业的心理蒙上了一层阴影。他们认为外企在中国已经不再受到欢迎。<br />
<br />
　　华尔街日报援引中国美国商会新近一项调查显示，随着旨在将外国科技企业排挤出中国市场带来刺痛，越来越多的美国企业感觉在中国不受欢迎。一些外资企业说，他们与中国的关系正在变味，因为更加强硬的政府政策和日渐加剧的国内竞争令中国这个全球最重要市场之一对跨国企业的态度不再那么友好。</div>

							
							
							
							
