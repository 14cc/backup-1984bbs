　　国庆60周年前夕，一项最新发布的社会调查显示，近95%的中国人表示，即便可以任意选择，“中国人”的身份依然是他们的首选，显示出国人极强的民族自豪感。<br />
<br />
　　92%受访者会看阅兵式<br />
<br />
　　复旦大学传媒与舆情调查中心于9月27日发布了最新出炉的“新中国成立六十周年全国九省市城乡公众记忆、认知与态度” 大型舆情调查结果。该调查在全国九省市成功访问了1018位年龄在16周岁以上的城乡公众。<br />
<br />
　　92%的被访者表示会收看10月1日上午在天安门广场举行的盛大阅兵式和群众游行；85%的被访者表示会收看当晚在天安门广场举行的联欢晚会；73%的被访者表示会观看（或已经观看了）国庆献礼影片《建国大业》；63%的被访者表示会收看为庆祝建国60周年专门创作的大型音乐舞蹈史诗《复兴之路》。<br />
<br />
　　“改革开放”印象最深<br />
<br />
　　调查发现，报纸广播电视等国内媒介是城乡公众了解共和国历史的最主要途径（选择比例达76%，该题为多选题），其次是书籍（38%）、学校教育（37%）、亲身感受（34%）和新兴的网络媒体（33%）。对于80、90后的年轻人，网络已经成为他们除电视（62.7%）之外了解共和国历史的第二大渠道（50.8%）。<br />
<br />
　　在回顾共和国历史时，“改革开放”成为各地城乡公众印象最深的共和国事件（提及比例为30%），其他提及较多的共和国事件还包括香港和澳门回归祖国、2008年北京奥运会、“文化大革命”等。<br />
<br />
　　农村生活提升感更明显<br />
<br />
　　本次调查请被访者就共和国60年来的发展打分，结果表明，如果以10分为满分的话，全国城乡公众打出的平均分为8.5分。其中，农村公众打出的平均分（8.6分）略高于城市公众（8.4分）。同时，随着年龄的增长，公众对共和国60年来的发展评价越高。<br />
<br />
　　调查显示，有98%的公众表示会“以自己身为一个中国人而自豪”，94.9%的公众表示“就算可以随便选择世界上任何国家，我也更愿意做中国人”。<br />
<br />
　　当被问及最近五年的生活变化时，86.7%的被访者表示最近五年的生活有所提高，其中表示“提高很多”的有41.2%，表示“提高一些”的有45.5%。相比于城市公众，农村公众更加感到自身生活水平的提高――有82.9%的城市被访者感受到近五年来生活的进步，而这一比例在农村上升为91.6%。有96%的公众对共和国未来十年的发展充满信心，其中表示“非常有信心”的占54.9%。</div>

							
							
							
							
