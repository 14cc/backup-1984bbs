													<h2>余杰：Hu Jintao为何拒绝这根救命的稻草？</h2>
						
						
																											<div id="postmessage_202111" class="t_msgfont">――读《零八宪章与中国变革》<br />
&nbsp; &nbsp; 由中国信息中心编辑、劳改基金会出版的《零八宪章与中国变革》一书，在“June Fourth”二十周年前夕于香港问世。此后仅一个星期，中共宣传部、教育部、新闻出版署等部门即下令查禁此书，并特别向全国各大学发布文件，严禁大学师生阅读与传播该书，号召大家揭发举报之。此后三个星期，《零八宪章》的主要起草者和组织者之一刘晓波先生，被当局宣布以涉嫌“煽动颠覆国家政权罪”正式逮捕。这一决定是在刘晓波被秘密警察非法绑架、监禁六个半月之后作出的。据与刘晓波会面的律师介绍，刘晓波本人对这样的结果并不感到意外，他早有为义受逼迫的心理准备，所以从秘密监禁的地点转移至看守所之后，他的精神状态良好，对面临的审判过程坦然受之。<br />
&nbsp; &nbsp; 究竟是谁将刘晓波送进了监狱？据我的推测，作出此决定的，不是别人，正是作为中共党魁和未经选举的国家元首的Hu Jintao。在当局非法绑架刘晓波之后将近七个月的时间里，中国民间社会和国际社会的抗议声音一浪高过一浪，有关信息当然会反馈到最高当局那里。究竟如何处理刘晓波，必然经过政治局会议的讨论，而Hu Jintao是最后作决定的人。Hu Jintao难道不知道，他作出如此决定，根本伤害不了刘晓波，也吓阻不了那些追随刘晓波、为中国实现宪政与民主而努力的中国公民；Hu Jintao难道不知道，作出这样的决定只能表明，他愚蠢地拒绝了《零八宪章》这根救命的稻草。Hu Jintao将本来是救命恩人刘晓波当作仇敌，齐奥赛斯库的命运将在不远处等候他，到了那个时刻，他再想回头请求刘晓波的帮助，已经来不及了。<br />
&nbsp; &nbsp; 《零八宪章与中国变革》一书，收入《零八宪章》发布之后各界人士撰写的六十多篇文章。第一部分为“我们支持《零八宪章》”，收入捷克前总统哈维尔、达赖喇嘛等人的声明，捷克人权奖、美国笔会自由写作奖等国际奖项的颁奖词，还有余英时、哈金等数百名海外华人学者的声明，以及包括多名诺贝尔文学奖得主在内的全球三百多名作家联署的要求释放刘晓波的信件等等。第二部分为“《零八宪章》与中国社会转型”，收入多名人士对《零八宪章》的价值与意义的分析，认为这份文件诞生在中国社会转型的关键时刻，其横空出世，乃是“传承常识，解困当下”，是“道义之上的宪政启蒙”，论者坚信“公民运动的理性力量必定战胜强权”。第三部分为“我们与刘晓波不可分割”，收入《零八宪章》的多名签署者对刘晓波的声援文章，以及刘晓波的妻子刘霞的呼吁书等。第四部分为“《零八宪章》与当局的反应”，该文件发表之后，国保、片警和单位领导对若干签署者或威胁、或恐吓、或利诱，无所不用其极，但有几十名被约谈者将整个过程记录下来并在网络上发表，以公开化的策略应对“地下党”的卑劣做法。第五部分为“《零八宪章》的争论与前景”，收入围绕具体内容展开的一些争论，如联邦制与原住民自决问题，如保障私有财产与清算贪官污吏财产问题，如转型正义与社会和解问题等，这些争论显示出，《零八宪章》并非一个凝固的、已经完成的文本，而是一个与中国社会的发展变化同步的、“正在进行时”的文本。<br />
&nbsp; &nbsp; “June Fourth”之后的二十年，中共一次次地拒绝政治体制改革，并以经济发展来掩饰越来越严峻的社会问题。在此背景下，《零八宪章》应运而生，对如何解决中国当下的危机与困境，提出温和的、富于建设性建议。《零八宪章》并不谋求以革命的方式推翻中共的统治，尽管它对中共统治的“不合法性”有清晰的认定；它倡导联邦共和与和平转型，并力求以最小代价和震荡来实现“宪政中国”。《零八宪章》的起草者和签署者，不是革命家与造反者，大都是充满理性的知识分子，香港作家、媒体人梁文道便是其中之一。在本书中，梁文道这位在大陆最具殖民地的香港公共知识分子，先是解释了自己签名之前的担忧：“我怕丢了工作，怕今天的香港没有传媒敢再请我。我怕在大陆被封杀，再也无法在内地的传媒上发表言论。”但他又反问自己说：“可是身为一个住在香港的中国人，我时常感到惭愧和内疚。大家同在一条船上，凭什么我要比内地的同行能够享受更大的空间？为什么他们要比我冒更大的风险？”他终于战胜了恐惧，签署上自己的名字：“人有免于恐惧的自由。所谓恐惧，往往有一部分是心理的幻觉，只要你走了过去，你就自由了。更何况，这条路上不只你一人；如果有百人、千人、万人，你还有什么好怕的呢？”由此可见，《零八宪章》的酝酿成形的过程，也就是关心中国政治民主的公民凝聚共识、战胜恐惧的过程。<br />
&nbsp; &nbsp; 那么，Hu Jintao为何将这样苦口婆心的劝说视若仇雠呢？他真的连分辨基本的是非、善恶的智商都不具备了吗？是权力的惯性、权力的独占、权力的傲慢，使他以《宪章》为敌。这种“敌人意识”最后使得他本人成为人民公敌。今天的中国共产党早已失去了实现共产主义乌托邦的雄心壮志，“稳定”成了“压倒一切”的最高目标。中共大力宣扬“维护安定团结的大好局面”，其实是确保共产党一党专政的地位。所以，共产党比任何人都害怕革命，共产党才是当今真正“反革命”的政党。共产党这一“反革命”的力量，却又是“革命”与“动乱”的根源所在――如今中国的困局，与晚清极为相似：一百多年前，流亡日本的立宪派思想家梁启超在《现政府与革命党》一文中指出：“现政府者，制造革命党之一大工厂也。……大小官僚以数万计，夙暮孳孳，他无所事，而惟以制造革命党为事。”再看今天，瓮安事件、石首事件、通钢事件以及西藏、新疆的民族冲突，不是刁民闹事，不是群众不明真相，更不是海外敌对势力的策划，而是中共自身的腐败政治和打压政策酿成的；不是地方官僚局部的腐败无能，不是基层干部将中央政府的好政策变成了坏政策，而是根子就在中央，就在中南海，所谓“上梁不正下梁歪”也。梁启超当年对满清当局的抨击，完全可以转移到Hu Jintao的身上：“革命党何以生？生于政治腐败。政治腐败者，实制造革命党之主品也。”<br />
&nbsp; &nbsp; 本来，今天的中国具备了和平转型的若干条件。研究巴西现代化道路的学者董经胜指出，二十世纪八十年代巴西军政权还政于民，是一种“通过交易的转型”，避免了激进的暴力革命。这种“通过交易的选型”的实现，有赖于以下五个条件：第一，威权政府的统治必须牢固并得到相当程度的支持；第二，威权政府能够控制“颠覆性的”威胁；第三，倡导民主的反对派必须接受威权政府确定的一些限制和规则；第四，程度有限的民众动员；第五，熟练灵活的领导水平。除了第五个方面“熟练灵活的领导水平”Hu Jintao不具备之外，其他条件中国目前基本具备。但今天具备，并不表明明天也能具备。中共当局的权威与合法性正在迅速流失，民间激进力量正在抬头，因贫富悬殊和社会不公造成的怨愤情绪正在滋长，时机一旦丧失，等待中共的就不是“通过交易的转型”，而是“通过解脱的转型”和“在权威政体崩溃或瓦解后的转型”了。那时，千夫所指的共产党想求为多党政体下的一党亦不可得了。<br />
&nbsp; &nbsp; Hu Jintao却没有这样的紧迫感。他控制着世界上最有钱的政府，多财善贾，长袖善舞，并不认为危机迫在眉睫。他也根本不加考虑，不改革或者错过改革的最后机会，将会有什么样的后果。当年，满清拒绝改革，便自取灭亡；如今，中共拒绝改革，难道就能万寿无疆吗？对于今日中国现状之严峻，时间之紧迫，改革之必要，《零八宪章与中国变革》一书中有深刻之论述。无疑，这是一本应当被中共政治局集体学习的好书，更是一本所有关心中国未来命运的中国公民和外国友人都应仔细研读的好书。今天，Hu Jintao似乎有充分的理由拒绝《零八宪章》，似乎有充足的力量将刘晓波打入黑牢，他却无法在明天避免遭遇齐奥赛斯库的厄运――在逃亡路上，七十一岁的独裁者齐奥赛斯库多次伏在汽车仪表盘上哭泣。当死刑判决下达之后，齐奥赛斯库的妻子埃列娜向行刑士兵喊道：“你们怎能向我们开枪，我曾经那么关怀你们，我是你们的母亲。”一个士兵回答说：“不，你不是我们的母亲，你是杀死我们母亲的凶手。”枪声骤然响起……这一幕，会在Hu Jintao及其家人的身上重演吗？<br />
&nbsp; &nbsp; 二零零九年八月一日，共军建军之日</div>

							
							
							
							
