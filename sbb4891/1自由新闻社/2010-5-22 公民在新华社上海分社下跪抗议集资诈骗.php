上海, 大案, 只字不提<br />
<br />
作者：讲讲清楚&nbsp; &nbsp;提交日期：2010-4-12 23:08:01 楼主<br />
<br />
　　上海发生24k连锁经济型旅馆特大集资诈骗案，牵涉到3 个多亿，600多位受害者，案发前上海多家主流媒体为该企业大张旗鼓的进行有偿宣传、连续大幅刊登广告（广告花费一千多万元)，并举办所谓的论坛、媒体见面会等，邀请上海有各种社会影响的人物、权威专家、名人、官员参与造势活动等。案发二年来，上海主流媒体却无声无息，此案从不见报，24k案件的背后好像有一个无形的保护伞罩着，处理此案的相关部门避重求轻、大事化小、小事化了。我们广大受害者在本案中始终处于弱势地位,信息及其不对称，无法知道更多的情况。同时我们广大受害者两年来采取了各种各样符合国家法律程序的措施，但收效甚微，且24K的其他犯罪分子仍逍遥法外，并更加肆无忌惮地采用各种手法继续坑害广大受害者，目前已影响到600多个家庭的生存和稳定。我们广大受害者在近两年的合法维权过程中发现以下一些问题及线索，并强烈要求政府监管机构对此进行彻查。<br />
　　　　 一、2005年3月前上海金冠酒店服务有限公司8000万人民币是否存在虚假注册资本问题？《刑法》第158条的规定、数额、后果和其他情节中，只要有一项达到犯罪的程度，就应追究行为人的刑事责任。<br />
　　　　二、朱福弟收购福州路“懿文大楼”中介费就用去了 6000万人民币是否有行贿受贿问题？<br />
　　　　三、金冠公司法人朱福弟等推销时提供的假产证、假购房合同、一房多卖、无房也卖、朱福弟的假身份证、假担保、假合同、假及后续的假上市等等是否属于诈骗问题？<br />
　　　　四、投资者2.32亿、银行0.6亿和明大广场入0.4亿共3.32亿资金及几年来的营业收入（24k自报每年盈利0.2亿多）检察院仅查到2亿多，还有1亿多及近二年来营业及其他收入去向不明的问题？<br />
　　　　五、朱福弟在台州制药厂投资了2000万元人民币以及在外省市买地买楼（超出经营范围），是否属于非法挪用资金问题？<br />
　　　　六、朱福弟在推销24k投资时，经常使用蛊惑人心的语言就是“你们小业主放心投资，xx、xxx等头面人物也投资了”，而在法庭上朱福弟说:“在案发前后，已还小业主2000多万人民币……”，请问这2000多万还给谁了，我们广大小业主都没收到，是否还给xx、xxx等头面人物了？这是一个应该引起纪委等相关部门非常重视的问题。<br />
　　　　七、24k案件的关联利益集团合伙诈骗是否要负连担责任和追加被告的问题？（上海亿佰年房地产公司――唐东升、上海明大投资管理有限公司――朱福人、华泰民丰担保公司――潘一海、24k连锁酒店――朱崇磊、朱杰、上海大顺道酒店管理有限公司――朱加炜及香港戴得梁行酒店管理有限公司等）<br />
　　　　八、上海各大平面媒体非法收取的广告费及推介服务费(一千多万元)是否应退还问题？<br />
　　　　九、目前24k管理层还在继续侵吞我们受害人的财产，一年多来不仅没有创造利润反而亏损几佰万。据2008.12.28《新闻晨报》报道金融危机意外带火经济型酒店,上海所有经济型酒店都赢利,且利润可观。那么 24k的亏损是否有“转移资金”问题?朱福弟名下八家空壳公司互开费用支出，具体内容以住宿费为主就是一个典型的佐证，维权小组在查账过程中还发现营业款项中有相当部分不入财务帐册、使用假增加费用开支、变相发放补贴工资、一年付费第二年再付费、每月提取折旧款、开办费等各种违法手段，人为造成连月持续亏损的局面，对国家存在偷逃税款现象，且直接侵害我们广大投资者的经济利益;是否还存在其他“转移资金”的问题？<br />
　　　　十、关于个人犯罪与单位犯罪问题<br />
　　　　最高人民法院《关于审理单位犯罪具体应用法律有关问题的解释》第一条规定，单位犯罪必须经单位集体决定或者由其负责人决定实施。负责人决定是指经过法律或者单位规定有权代表单位思想和行为的个人决定。朱福弟是上海金冠酒店服务有限公司是法定代表人，有权代表单位思想和行为，符合单位犯罪的特征。<br />
　　　　十一、24k案件没有这么简单，朱福弟四年前在上海突然发迹，本身就是一个迷，有这么多的主流媒体帮他大势渲染，他用骗来的钱，出手大方借助专业协会名义，以24k赞助出资的办法，举办所谓的论坛、媒体见面会等。邀请上海有各种社会影响的人物、各大媒体大张旗鼓的有偿宣传（借官方媒体公信力）和邀请权威专家、名人、官员参与的造势活动等。其中是否存在权钱交易，官商勾结之嫌疑问题？<br />
　　　　十二、24k案件在开庭审理过程中，出庭做证的证人均与被告有共同利益人员或与被告存在亲属关系人员，其证词是否合法问题？<br />
　　　　<br />
　　　　24k案件的特例典型就是朱福弟用“金钱”打开一切关节，其内幕至今还未揭开，以上问题仍然没有一个说法。广大受害者认为朱福弟及其利益集团犯下多项罪行（诈骗罪、行贿罪、虚报注册资本罪、非法资金挪用罪、贪污罪、偷漏税罪等，均有据为证）。可24k目前仍在继续经营，这种局面是否合法？难道朱福弟等利益集团有豁免权，可以凌驾于法律之上？<br />
　　　　<br />
　　　　由于以上种种问题制造的假象，造成广大小业主上当受骗是事出有因，请法院充分考虑这一点，为我们600多个受害人主持公道，制订严密追赃计划，加强追缴赃款的力度，将广大受害者的血汗钱（动迁款、下岗职工工龄买断钱、老人一生的养老钱、抵押房产的贷款钱、家中身患绝症病人的救命钱等等………）损失降到最低。<br />
