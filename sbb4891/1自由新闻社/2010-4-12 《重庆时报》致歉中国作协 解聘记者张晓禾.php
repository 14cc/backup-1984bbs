中国作协七届九次主席团会、七届五次全委会在重庆召开，众多全国一流作家会聚重庆，是中国作协对重庆的重视和青睐，对重庆文学事业的大力支持，是重庆文坛的一件盛事。作为重庆的一家媒体，本应负责任地当好东道主，充分报道此次会议盛况，宣传会议丰硕成果，展示众多名家风采，以表达重庆人民的热忱和情谊。市委宣传部对此次会议的宣传报道也作了总体安排并提出明确要求。但是，没有引起我们足够重视。编委会在总体安排、报道策划、组织实施、人员调配上不到位，致使报道方向出现严重错误，完全背离了会议主题；记者在采访中道听途说，不深入了解、不认真核实，导致基本事实严重失实；值班编辑不仅不从稿件选题和内容上严格审核，反而采用娱乐化对比手法包装新闻，版面编排导向错误；值班副总编政治敏锐性不强，政治责任感缺乏，没有严格把关，致使失实报道见报；加之该报道经过其他媒体和网络转载放大，给中国作协和与会作家造成难以挽回的恶劣影响，教训十分深刻，我们深表痛心和自责。稿件见报后，本报对此高度重视，深刻反思，经认真调查核实，对相关责任人作出了严肃处理：主要撰稿记者予以解聘；联合署名的另一记者给予严重警告处分；文娱新闻室主编予以免职；分管副总编辑停职检查；总编辑作出深刻检查。<br />
<br />
我们将深刻吸取这一教训，举一反三，亡羊补牢，坚持“政治家办报”原则，牢牢把握正确舆论导向，增强政治意识、大局意识、责任意识，坚决克服新闻娱乐化倾向，将正确的办报理念和编辑思想贯穿到新闻采编的每一个环节；狠抓领导班子和采编队伍建设，不断深化中国特色社会主义理论体系、马克思主义新闻观、职业精神职业道德的“三项学习教育活动”，造就一支政治强、业务精、纪律严、作风正的采编队伍；狠抓内部管理和制度建设，按照媒体转型提档升级的目标要求，层层抓落实，人人负责任；在今后的宣传报道工作中，切实履行新闻媒体的神圣职责，见微知著，防微杜渐，杜绝类似事件再度发生。<br />
<br />
这起严重报道错误给我们上了深刻的一课。今后，我们将大力宣传中国作协及作家们以优秀作品反映现实、讴歌时代，丰富广大人民群众精神文化生活的巨大努力；大力宣传作家们深入实际、深入基层、深入群众进行创作，勇于担当社会与人文责任的感人事迹；大力宣传中国作协为弘扬优秀民族文化、展示当代中国风采、推动我国文学事业繁荣发展做出的积极贡献，以实际行动为中国作协和作家们的辛勤劳动、丰硕成果鼓与呼。<br />
<br />
再次向中国作协和与会作家致以深深的歉意！<br />
