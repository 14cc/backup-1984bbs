													<h2>湖北省长李鸿忠撰文称要有听取逆耳之言的雅量</h2>
						
						
																											<div id="postmessage_806451" class="t_msgfont">人民网-人民日报9月1日报道 党的十七大报告指出，要扩大人民民主，推进决策科学化、民主化。建立政府参事制度，是新中国在国家政权建设和社会主义民主政治建设方面的一个创举，为推进政府决策科学化、民主化作出了重要贡献。去年，国务院正式颁布《政府参事工作条例》(以下简称《条例》)。这是我国政府推进决策科学化、民主化的重要举措，标志着政府参事工作迈入规范化、制度化、法治化的轨道。深入贯彻落实党的十七大精神和《条例》有关要求，需要我们深刻认识参事工作的重大意义，充分保障参事作用的有效发挥，努力推动参事工作再上一个新台阶。<br />
<br />
参事工作意义重大<br />
<br />
湖北省政府参事室成立于1950年初。自参事室成立以来，历届参事始终坚持与党和政府风雨同舟、与社会主义建设和改革发展进程紧密相连、与推进社会主义现代化建设的宏伟大业同频共振，在参政议政、建言献策、统战联谊等方面做了大量富有成效的工作。参事们围绕中心、服务大局，密切关注民生和经济社会发展中的重大问题，通过调查研究、反映问题、提出建议等多种方式积极履行职责，对政府工作发挥了重要的决策咨询作用，对省委、省政府了解社情民意发挥了重要的渠道沟通作用，对团结各方面力量发挥了重要的统战联谊作用，为全省改革开放和现代化建设作出了突出贡献。特别是近几年来，参事们围绕“两圈一带”(武汉城市圈、鄂西生态文化旅游圈、湖北长江经济带新一轮开放开发)建设、经济结构调整、重点产业布局、改善投资环境、加强重点项目建设、应对国际金融危机、农村劳动力转移就业、农民工权益保障、集体林权制度改革、三峡库区地质灾害防治、新型农村养老保险体系建设、企业技术改造、城镇居民基本医疗保险制度建设、公务员能力建设等经济社会发展中的突出问题深入开展调查研究，提出100多份切实可行的意见和建议，有效地推进了政府科学民主决策。<br />
<br />
参事工作意义重大，参事职责神圣庄严。“参”，就是参与、参谋、参议；“事”，就是从事、理事、做事，做对政府工作有利的事，做对促进经济社会发展有重要意义的事；“参”与“事”合起来，就是给政府决策当“高参”。湖北省一直非常重视参事工作，在参事的选聘、履职和管理、服务等方面制订了一系列规章制度。目前，省参事室共有58名参事，其中有两院院士、大学教授、科研院所高级研究人员，也有国有大型企业高级技术人员、管理人员和具有高级专业职称的专家型行政管理人员，已经成为湖北省改革开放、经济社会发展与统一战线的一支重要力量，成为省委、省政府信得过、用得上、离不开的智库和帮手。实践证明，参事室是集聚贤才、富有谋略的智囊机构，参事工作是反映民情、集中民智的重要渠道；做好参事工作，对于发展社会主义民主政治、巩固党的执政基础，对于推进政府职能转变、促进科学民主决策，对于推动科学发展、促进社会和谐，都具有十分重要的意义。<br />
<br />
面对新形势新任务，必须进一步做好参事工作。当前，湖北省正面临中央实施促进中部地区崛起战略、建设“两型”社会的难得机遇，经济发展正处于由回升向好向稳定增长转变的关键时期。在这种形势下，如何抓住机遇、应对挑战，实现又好又快发展，是对政府工作的一个考验。因此，必须凝集各方面的力量和智慧，包括进一步发挥参事的作用。参事队伍是一个团队、一个智库，参事进行参政议政、建言献策是对政府工作非常难得的智力支持。“知屋漏者在宇下，知政失者在草野”。政府工作千头万绪，政府工作人员不可能对所有工作都考虑得非常周全。参事身份超脱、联系广泛，可以相对客观地看待、评价和监督政府工作，对政府工作提出改进意见和建议，从而有利于政府更好地履行职责，推动改革发展，造福人民群众。<br />
<br />
参事工作务求“三真”<br />
<br />
在《条例》颁布之际，温家宝同志与国务院参事、中央文史馆馆员座谈，听取意见和建议，强调“参事室作为政府的咨询机构，在建言献策方面应保持相对的独立性，说真话、讲实情，允许发表不同意见和看法，不唯上、不唯书、只唯实。”这对做好新形势下的参事工作具有重要指导意义。作为政府的高参和智囊，参事应按照《条例》要求，力求做到“三真”，即贡献真才实学、反映真实情况、敢于讲真话。做到“三真”，必须坚持解放思想、贴近实际、敢于直言。<br />
<br />
坚持解放思想。解放思想是发展中国特色社会主义的一大法宝，也是做好参事工作的重要前提。改革开放30多年来，我国经济社会发展之所以取得巨大成就，就是因为我们坚持思想解放，坚持改革创新。面对新形势、新任务，我们要抓住机遇、应对挑战，实现科学发展，必须继续解放思想。在参事工作中坚持解放思想，就是围绕党和政府的中心工作，研究新情况、解决新问题、总结新经验，为不断开创各项事业新局面提供智力支持，同时努力把参事事业不断推向前进。只有坚持解放思想，所提的意见和建议才能闪烁出智慧光芒、创造出实际效益。<br />
<br />
坚持贴近实际。参事工作的目标非常明确，就是要有利于决策、有利于发展、有利于落实、有利于解决问题。因此，参事工作必须有针对性。省政府参事要针对省情、贴近省情，在研究解决本地区经济社会发展重大问题的过程中提出自己的看法，形成真知灼见。参事工作还要注意侧重于应用，以用立说，以用立论，以用立业，坚持理论联系实际，着眼解决实际问题。湖北省目前面临的最大问题是发展不够，面临的突出任务是加快发展。省政府参事应围绕这一问题，深入开展调查研究，勇于提出源自实际、切实可行的意见和建议。<br />
<br />
坚持敢于直言。参事工作要围绕党和政府的中心工作建言献策，但不能只是简单地为党委、政府的决策作注释、作论证。参事要以深入实际、联系群众，客观公正地反映社情民意为职责。反映的情况要客观真实，原汁原味。真话一句胜过假话连篇。只有掌握和反映了真实情况，才能促进科学民主决策，真正发挥高参和智囊作用，有效履行党和政府赋予的职责。政府负责人要鼓励参事独立思考，敢于发表不同意见。良药苦口利于病，忠言逆耳利于行。对参事提出的意见和建议，要有听取逆耳之言的雅量，能包容不同意见，择其善者而从之。<br />
<br />
对参事要高看一眼、厚爱一分<br />
<br />
《条例》的颁布，使参事工作和参事履职尽责有了法律保障和依据，有利于调动参事的积极性。《条例》的颁布也对政府工作提出了更高的要求。政府及相关部门应该自觉用《条例》来规范参事工作，为参事工作的开展营造良好的环境。科学发展需要科学决策，科学决策需要民主参与。我们要按照《条例》的精神和要求，进一步加强和改进参事工作，更好地发挥参事的参谋助手作用，努力实现湖北经济社会又好又快发展。<br />
<br />
