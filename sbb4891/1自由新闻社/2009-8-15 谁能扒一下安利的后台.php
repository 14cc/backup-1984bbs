<br />
一般来说，安利是以开发亲友的购买潜力为宗旨的。此公司一直宣扬的一个“圣经”故事是：在很久很久以前，有一个老奶奶用了某产品，觉得很管用，于是逢人就讲，一传十十传百，导致此产品的销量剧增.......这使当时的销售人士注意到，传销者越不懂科学知识，越感性，就越富有蛊惑力.......这奠定了这类传销事业的理论基础。<br />
<br />
至于安利，则以此为基础不断的在人性弱点上下功夫：<br />
一，专攻没有专业知识的底层民众，开课讲授所谓的专业知识。比如将安利的洗涤类产品和其它产品做比较时，就搞科学小实验，在同样份量的洗涤剂中加水，宣扬泡泡越多，产品越好........<br />
二，利用底层民众希望自己和家人过上好日子的迫切心理。销售经理一边讲课一边吹自己生活多幸福，常常坐飞机，和家人去海边度假.......不断灌输与会者，只要加入安利，就能过上同样的好生活.......<br />
三，安利入会要自己先买数千元的产品试用。所谓的，自己先要有体会才能感染周围的人.......所以安利的销售额多数都是“内部消费”。从外部看，安利其实是将员工和顾客一体化。而且一体化有个好处，就是即便作为顾客觉得安利产品一般，作为员工，尤其是作为一个即将要剥削下线的隐形&quot;股东&quot;，又立刻会跳出来说服自己。<br />
