中国政府施压墨尔本国际电影节・ 澳洲人士从力拓案看澳中贸易<br />
听众朋友，您好，欢迎您收听今天的时事报道节目，我是严力。<br />
<br />
今天的节目后半部，我们将为您报道澳洲有关人士探讨从胡士泰被捕案看澳中贸易，由澳广中文部记者田方编播。不过，首先让我们来关注占据今天澳洲媒体头版的新闻，那就是据称中国驻墨尔本领事馆官员试图阻止一部关于热比亚的纪录片在墨尔本国际电影节上播放。<br />
<br />
中国新疆乌鲁木齐暴乱事件使流亡海外的世界维吾尔人大会主席热比娅成了世界各地的新闻人物。就在这个时候，即将在下个月开幕的墨尔本国际电影节上的一部不显眼的影片《爱之十诫》(Ten Conditions of Love)却引起了澳洲各大媒体的注意。这是一部有关热比娅的纪录片，而媒体对这部影片特别关注的原因是因为据电影节的负责人说，中国政府试图阻止这部影片的放映。<br />
<br />
理查德&#8226;莫尔是墨尔本国际电影节的总指导之一。<br />
<br />
“星期五那天，我收到中国驻墨尔本领事馆一位陈女士的电话。她说她打电话是督促我把这部电影从电影节上撤下来。我说我没有任何理由这样做。这位女士说那我有什么理由要在电影节里放这部电影呢？我回答说，对不起，我不需要给出任何在电影节上播出这部电影的原因。”<br />
<br />
电影节不但将播放这部纪录片，这部片子的导演也邀请了热比娅本人来墨尔本参加影片的首映式。电影节指导莫尔说，热比娅并不是电影节邀请的，她是影片导演的客人，电影节不会拒绝她的来访。<br />
<br />
“我想中国领事馆的这位女士想说的主要就是对中国政府来说，热比亚是一个罪犯。她还对热比亚将来墨尔本参加电影节非常不满。她不停地重复我们应该把这部电影撤下来。最后我只能对她说，对不起，我已经表明了我的立场，然后我就很礼貌的把电话挂了。”<br />
<br />
影片的导演丹尼尔斯来自美国纽约，现在居住在墨尔本。他说，他拍这部电影的初衷是因为他亲身经历了9&#8226;11事件，随后美国开始向“恐怖主义”宣战。而中国政府指称热比娅等人和恐怖组织有关，他想通过他的这部电影探讨到底什么是恐怖主义，它的定义又是谁来决定。<br />
<br />
丹尼尔斯说，尽管他已经预料到中国官方不会喜欢这部电影，但是他对政府会公然要求国际电影节组委撤下这部电影感到惊讶。<br />
<br />
“我对中国政府不想让这部电影上映并不感到奇怪。中国政府认为热比娅支持恐怖组织，她是世界维吾尔大会的主席，中国政府认为这个组织和恐怖组织有关系，所以我并不奇怪中国政府不想让这部电影上映。但是让我惊讶的是他们居然公开要求（电影节）不要放这部电影。他们难道没有想到这会引来媒体更多的注意吗？”<br />
<br />
丹尼尔斯说，中国政府应该采取更开明的态度来对待这件事。<br />
<br />
“我不明白为什么他们不愿意在平等的基础上表达他们的观点，而不是像现在这样试图阻止其他人不同的观点。我没有想到中国政府会觉得热比亚对他们的国家安全是一个这么大的一个威胁。我很愿意中国政府代表也来参加电影节，向观众表达他们的观点，但是我想他们是不会来的，因为在我拍摄这部电影的过程中，我曾经联系了在纽约、华盛顿和堪培拉的中国使馆，请他们发言。但是每次一提到热比娅的名字，我们的谈话就嘎然而止。”<br />
<br />
国际风险评估公司香港分公司的维克斯说，中国对热比娅警惕性如此之高是因为新疆目前的局势对中国政府来说很严峻。<br />
<br />
“新疆以前发生的一些骚乱都是小打小闹，但是这次乌鲁木齐暴乱死了这么多人，而且死的大多是汉族，这把新疆的局势推到了一个新的高度上。中国政府视此为一大挑战，毫无疑问他们会在未来的数月内采取很严厉的措施处理这个问题。”<br />
<br />
维克斯说，新疆问题除了有民族冲突的因素之外，新疆有大量的石油和能源储存，保护这些能源对中国政府来说也是非常重要的。<br />
<br />
“一场暴乱导致一百八十多人死亡，这在世界哪个国家都是很严重的事件。所以对中国政府来说，整顿社会治安是当务之急。而从长远来看，新疆有丰富的石油和天然气储藏。如果叛乱分子在这个地区得以发展壮大，那么这些资源是很难保护的。就像我们看到的发生在其它一些国家的问题，一旦有这样的叛乱分子存在，保护石油运输管道就变得很困难。而新疆的能源储藏和石油管道运输对中国是非常重要的。中国政府怎样在未来应付这个问题值得我们的关注。”<br />
<br />
