贾樟柯等的政治意淫纲要 / 肖川<br />
2009-07-25 10:21 | 阅读(112) | 标签: 时事观点, 所见所闻, 中国, 民主, 言论自由, 政治, 媒体, 政府, 真相 | 字号：大 中 小 <br />
 <br />
<br />
最近中国导演退出墨尔本国际电影节的事情很火，其实也就四位导演。而中国媒体觉得这种新闻有必要做爱国宣传教育，所以大多数标题都是在玩文字，比如最明显就是“贾樟柯等退出墨尔本电影节”。在人物后面加等是什么意思，相信接受过初中语文教育的都知道，这表示后面还有很多的意思。所以这些新闻在刚开始看的时候，我以为中国有一帮导演退出墨尔本影展，有张艺谋周星驰赵薇陈凯歌他们。哦，对了，凯歌好像已经不是中国人了。这样一宣传造势，都是从官方网站出来的，不正确也旗帜鲜明了。所以贾樟柯等导演的电影也许在墨尔本拿不到奖，但在国内绝对是民心所向的，又是一次未映先红。<br />
<br />
 <br />
<br />
<br />
再来说一说这次贾樟柯的问题。注，在此先提醒一下，贾樟柯的电影我是喜欢的。贾樟柯等导演退出墨尔本影展是由于墨尔本影展主办方展出了一个关于热比娅的纪录片，同时主办方还邀请纪录片主人热比娅参加。其实如果平时墨尔本影展展示出关于热比娅的纪录片，我相信贾樟柯等他们的那几个导演是不会说退出影展的，相反可能找尽一切机会来宣传造势自己带去的片子。而现实是，中国新疆发生乌鲁木齐事件，报道称是以热比娅为首领导策划的反华势力造成的。所以热比娅就是中国的敌人，贾樟柯等是中国人所以必须在参加影展的时候采取避嫌的态度对待。如果这次就陈凯歌去，后果怎么样也不好说，但估计凯歌不会退出吧？<br />
<br />
 <br />
<br />
<br />
再说热比娅，请问有多少大陆人知道。我反正是不知道的，至少在新闻刚出来的时候，我还以为热比娅是一个像埃塞俄比亚那样的地方而已。看来三遍左右，通过分析热比娅是一个人，可是想深入了解一下这个反革命同志，网易关闭了评论，我不知道问谁去。<br />
<br />
 <br />
<br />
<br />
正好，这次墨尔本影展展出关于热比娅的纪录片，我想这是否也是墨尔本主办方想告诉贾樟柯等一些导演关于对热比娅的认识呢？因为热比娅既然是反革命，所以关于热比娅的资料，你在大陆地区应该是封锁得很严格的。所以我们不知道，贾樟柯等一些导演，你们是怎么知道的，莫非你们都擅长翻墙？<br />
<br />
 <br />
<br />
<br />
所以影展应该是可以展出一切影片的，反正都是一帮搞电影的，你们那些破事，相互之间谁不知道谁半斤八两的。所以称墨尔本影展被政治恶臭了，不过是贾樟柯等几个导演意淫出来的。政治层面的意思是生硬给套上去的，就好比某些写时评的作家，生硬给套上一个反革命一样。这事情如果不是发生在乌鲁木齐事件之后，我想贾樟柯等导演是不会想到政治避嫌的。但现实是，这事发生在关键事情，所以必须政治立场明确。而对于国内等很多人表示支持，我想这应该无关爱国的立场吧？如果你生硬说这是爱国的做法，那么我说重庆力帆与安徽九方群殴事件因为一个外援被侮辱而反抗结果引起群殴也是爱国行为。你外援在中国踢球，你受了委屈你也敢叫，我就是打你这样的黑鬼，咋了，中国地盘不管有理没理我们就做主，我们这是爱国行为，这是民族精神的体现。爱国跟政治避嫌不一样，不要给自己带上一个糊弄国人的高帽子，以为自己就是卫士了，那样是卫道士还有点像。真正意义的爱国，是我们的国家权益遭受侵害的时候，比如xx国一导弹炸了我国任何一点领土，我们就要奋起反抗。但当年共产党在国内打国民党那就不是爱国，那是反贪。我说的，你明白吗？所以贾樟柯等导演，这次如果真正考虑政治意义，那么他们应该认真看完影片，然后如果发现影片当中有胡编乱造歪曲事实的事情，就当场集体站出来然后抗议，让全场记者都知道，把热比娅当场炮轰出去。<br />
<br />
 <br />
<br />
<br />
