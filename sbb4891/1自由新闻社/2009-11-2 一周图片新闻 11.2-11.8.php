昨日（2009年11月3日），东莞特大涉黑案在东莞市中级人民法院开审，该团伙头目被控犯组织、领导黑社会性质组织罪等8项罪名。该团伙成员达60多人，在长安上沙一带称霸一方，强收保护费、敲诈勒索、暴力抢劫，初步被查出共犯下各类案件26宗。 <br />
<br />
　　昨日上午，东莞市中级人民法院大法庭内，公诉人将装满卷宗的购物车推入法庭，19名被告身着囚服，挤满了被告席。其中13人是80后，团伙头目王琳1983年出生，贵州人。 <br />
<br />
　　检方指控，该涉黑组织近3年来活跃于长安上沙、沙头、厦岗等社区。其头目王琳2006年自立门户后，迅速网罗了一批刑满释放人员及社会闲杂人员，通过暴力、威胁或开设赌场、放高利贷、替人收债等手段，获取经济利益，长期有组织地实施抢劫、故意伤害、敲诈勒索、寻衅滋事、聚众斗殴、非法拘禁等违犯罪活动，称霸一方。<br />
