<br />
　　据被告此前供述，王琳为了树立威信，巩固老大地位，多次假借生日之名，迫使众多马仔和几个社区的商家服从其管理及非法保护。王琳对成员管理严格，如教训不参加生日会的成员，驱赶擅自冒其名义收取保护费的成员，殴打清除不服从管理的成员；同时，王琳还以借钱吃饭、帮人出头、安排看场等手段笼络组织成员。 <br />
<br />
