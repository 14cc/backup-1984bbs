<br />
　　在1979年，一群不被社会重视的青年艺术家组织了中国第一个独立的当代艺术展览，并成立“星星画会”(The Stars)。“星星”运动的领导者之一黄锐在宣言中说道：“天空中的星辰与太阳平等，每一颗都闪烁光芒。”他挑选了30年中具有代表性的5件中国艺术品。<br />
<br />
<br />
<br />
<br />
<br />
<br />
