　　昨日（2009年11月2日）上午，一名只有六个月大的婴儿在番禺市桥医院输液后死亡。家属以婴儿非正常死亡为由，召集近百名亲属及老乡围堵医院要求作出解释，并一度打砸病房。直到昨日下午6时，警方出动数十特勤、辅警，从现场带走14名人才暂时平息事件。 <br />
<br />
　　<strong>现场：十余病房被砸</strong> <br />
<br />
　　昨日中午12时左右，数十名福建籍人冲进番禺市桥医院，要求院方就一名六个月大男婴在该院打针后死亡作出合理解释。 <br />
<br />
　　目击者称，前来讨说法的，从20人左右渐渐增加至60多人，都是三四十岁的青壮年，“进医院后就把大门堵住，不让人进出”。 <br />
<br />
　　在院方没有及时回应的情况下，死亡男婴家属及老乡冲进七楼病房，拿起凳子等就开始打砸玻璃与医疗器械。“动作快，很粗暴。”目击者称，当时医生还在抢救那男婴，其它病房有十多名病人。 <br />
<br />
　　番禺警方称，他们12时许接到报案，称有人在医院闹事，赶到现场时，七楼病房已经散落了一地的玻璃，十多间病房的门窗被砸坏，一些医疗器械扔得到处都是，有人仍试图继续打砸。 <br />
<br />
　　昨日下午，一名老者抱着一名死亡男婴坐在医院门口，中间是一大群自称是福建籍的老乡，男婴的亲属不断向人介绍事情经过；医院门口两侧站满了身着制服的公安特勤和辅警。 <br />
<br />
