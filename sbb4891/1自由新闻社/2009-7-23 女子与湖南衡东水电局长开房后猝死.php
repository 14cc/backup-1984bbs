目前，吕军已被停职接受调查。<br />
<br />
当日下午4时，衡东县公安局110指挥中心接到衡东宾馆工作人员电话，称一名女子在宾馆开房休息出来后，行至宾馆后门口时突然倒地身亡。该县公安局接警后，迅速赶往事发现场调查。经查，死者系衡东县工商局女职员唐某，今年38岁，事发前，与该县水利水电局局长吕军在宾馆开房休息。“大概是下午2点开的房，将近4点离开。”一位知情者称，吕军先行离开，而后唐某走出宾馆，当时太阳很大，就在她撑伞准备离开时突然倒地身亡。<br />
<br />
事情发生后，该县政法委、公安局主要领导第一时间进行现场处置。“迫于压力，吕军主动向公安机关交待了情况。”当地一政府官员称，当晚，衡东县委、政府召开了紧急会议，安排县政法委、县纪委、县公安局全力彻查，并决定对吕军停职接受调查。16日，根据死者亲属要求，县里请来省、市专家进行尸检。<br />
<br />
衡东县委宣传部表示，在唐某死亡原因出来之前，网络上各种有关其吃了春药等言论均属猜测，其在上班期间内，吕军是否利用公款开房，纪委也在调查中。由于尸检还没有结果，吕军是否应对唐某的死亡承担刑事责任，也暂时难以认定，但可以肯定的是其生活作风有问题。待尸检结果出来后，将会对吕军作出相应的处罚。<br />
