													<h2>民主政治与社会稳定------张雪忠</h2>
						
						
																											<div id="postmessage_418347" class="t_msgfont">在中国大陆，有些学者一直在重复着一种论调，即民主政治不利于保持社会稳定，因此中国不应该推行以自由选举及多党制为特征的民主政治。这些持“民主不稳定论”的人，总是喜欢举出一些民主社会的具体事例，来“证明”其观点。<br />
<br />
　　泰国的街头政治、台湾的蓝绿冲突以及苏联的解体，都是他们津津乐道的话题。但这些人似乎从来都不愿想一想：在自己举出这些有利例证的同时，那些持相反观点的人是否有可能举出同样多的反例呢？<br />
<br />
　　如果泰国的事例可以证明民主政治容易导致社会动荡，缅甸的更加血腥的动乱不是可以证明相反的结论吗？这种矛盾甚至在同一个国家也不可避免。朝鲜井然有序的首都广场和北京奥运会的成功，可能会使人得出一种结论，但从边境不断逃离的朝鲜居民和中国的瓮安事件，又可能使人们得出另一种相反的结论。<br />
<br />
　　前后一致是一名学者的最大责任，但这一点在那些主张“民主不稳定论”的中国学者身上却极少见到。如果能够做到这一点，面对这些相互矛盾的例证，他们就不可能像现在这样对自己的观点充满自信。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;认识方法的错误<br />
<br />
　　仅就理论研究而言，这些人所犯的最大错误还是在方法方面。实际上，不管是在民主社会，还是在非民主社会，都会出现各种各样激烈程度不同的社会或政治争执。如果两种具有相反佐证作用的事例在数量上均难以穷尽，人们就不可能指望通过举例和归纳的方法得出可靠的结论。<br />
<br />
　　在中国，那些反对民主政治的人最不厌其烦地使用的例子，还是苏联的解体。在这些人眼里，戈尔巴乔夫推行的民主化改革是苏联解体的罪魁祸首，似乎之前七十年的极权体制对此毫无责任。这种将与某种结果在时间或空间上最接近的因素，想当然地视为这一结果的最主要原因的做法，是作为学者的视野狭隘和洞察力不足的显著表现。<br />
<br />
　　当问题涉及事物的那些不能直接表现在日常经验中的属性时，这种做法最容易发生。而在简单的日常经验中，这种做法的荒谬却能够一览无遗。例如，当一个人在被他人重伤后经抢救无效而死亡，那些在死者生前最后时刻对伤者全力施救的医务人员，难道应该被视为杀人凶手吗？<br />
<br />
　　先是精心选择一些有利的事例，然后再从中归纳出早已成竹在胸的结论，这种做法不可能真正揭示出民主政治与社会稳定（或不稳定）之间的必然关系。在归纳方法不可行的情况下，人们只能首先考察与政治制度及社会稳定最密切相关的因素，并在此基础上推导出相关结论。<br />
<br />
　　政治制度事关一个社会人与人之间的政治关系安排。一种制度能否保障社会稳定，显然与人性密切相关。需要指出的是，通过假定人性具有某种特定的属性所得出某种独断性的结论，并不会有足够的说服力，因为不同的人可能对人性具有不同的看法。<br />
<br />
　　但是，如果不只是假定人性具有某种特定的属性，而是一一考察人性在这方面的全部可能情形，所得出的结论则将具有先验的必然性。人性在这方面的属性只有三种不同的可能，我们不妨对它们一一进行考察。<br />
<br />
　　第一种可能是，每个人都希望在政治上相互平等，既不想统治他人，也不想被他人统治。在这种情况下，人与人之间的政治不平等状态违背了所有人的意愿，因而不可能长久保持。只有保障个人政治平等的民主政治，才能带来长久的社会稳定。<br />
<br />
　　第二种可能是，每个人都想统治他人，而不想被他人统治。在这种情况下，不平等的政治安排必然导致无休止的抗争，而保障政治平等的民主政治尽管会让每个人都不能感到完全满意，但却是人们最有可能共同接受的结果，因而也是最可能保持社会稳定的政治安排。<br />
<br />
　　第三种可能是，一部分人希望统治他人，另一部分人则希望被他人统治。这种情形接近于亚里士多德的看法，他认为人类根据天性来说，有些人更宜于“治人”，另一些人则以“治于人”为宜。霍布斯直截了当地批驳了这一说法，他认为这一说法不但违反理性，而且违反经验，因为世间很少有人会愚蠢到不愿自己管理自己的事务而宁愿受治于人。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;维持稳定的还是民主制度<br />
<br />
　　但即使假定亚里士多德的看法符合事实，人与人之间的政治不平等仍无法带来社会安宁，因为在一个人数并非屈指可数的社会，人们几乎不可能将这两类不同的人一一甄别出来，并使他们各得其所。在这种情况下，能够维持社会稳定的，还是保障个人政治平等的民主制度。因为，那些甘愿受他人统治的人始终可以自愿的方式服从他人，而在那些具有统治他人的偏好的人之间，政治平等仍是最可能被共同接受的安排。<br />
<br />
　　通过上述分析，人们可以看到，自人性角度而言，民主政治始终是确保社会稳定的必然选择。在民主社会，由于每一个人都可以充分地表达自己的政治立场和对社会问题的看法，因此必然时刻都充满着吵闹与争辩。<br />
<br />
　　但在这种看似永无宁日的社会，分歧其实更多是技术性的，人们往往不会质疑政治制度本身的合法性，各种社会问题可以通过辩论和说服而得到和平的解决。民主的政治制度在提供根本性的社会稳定的同时，也为不断解决各种社会问题预留了制度空间。<br />
<br />
　　在非民主政治条件下，整个社会在表面上可能显得风平浪静，但这并不是因为人们认同这种政治制度，而是因为存在一刻也不能放松的政治压制。人们对政治权力的服从不是出于认同，而是出于恐惧。但源于恐惧的服从必然包含着愤恨与不满。<br />
<br />
　　在非民主社会，无处不在的政治压制不但使社会问题无法得到及时的解决，而且本身就是社会冲突的根源。如果说在民主国家维持国内秩序往往只需要警察，在非民主国家则必须经常出动军队，尽管军队本应用于捍卫国家利益，而不是用于国内政治斗争。<br />
<br />
　　民主政治与非民主政治的区别，还在于能否与社会稳定相互促进。一个社会实行民主政治的时间越长，社会稳定往往越牢固。社会稳定不再成其为一个重要的政策目标，而是制度运作的自然结果。<br />
<br />
　　相反，一个社会实行非民主政治的时间越长，社会矛盾和政治抗争就越发频繁和激烈，社会稳定问题就显得越发迫切。但如果一种政治制度必须将社会稳定视为压倒一切的政策目标，那只能证明这种制度具有催生社会不稳定的本性。<br />
