																											<div id="postmessage_418430" class="t_msgfont">中国政治改革的核心问题<br />
2007年8月31日发表于联合早报）<br />
● 张雪忠（上海）<br />
<br />
　　在***十六大报告中，***前总书记江『』『』曾提到，“必须在坚持四项基本原则的前提下，继续积极稳妥地推进政治体制改革”。今年6月，***总书记胡『』『』在中央党校省部级干部进修班发表讲话，也谈到政治体制改革必须坚持正确的政治方向，要坚持党的领导。<br />
<br />
　　自秦朝至今，贯穿中国政治文化与政治制度的一直都是这样一个问题：一个国家或社会应该由谁来统治？秦王嬴政灭掉六国之后，自以为功高“三皇”，德过“五帝”，故自命是秦“始皇帝”，并竭力要建立其家族万世一系的统治，其子孙也将是“二世”、“三世”，以至无穷。秦朝之后的历朝历代也无不为维系万世不替的统治而绞尽脑汁。<br />
<br />
　　一旦“应该由谁来统治”成为政治生活中的核心问题，整个社会必然会分裂为统治和被统治的不同阵营，而那些据说注定要统治他人的人也必然是以形形色色的面目示人的“精英”。他们要么是柏拉图笔下能够防止社会衰败的哲学王，或是黑格尔眼中体现了时代精神的普鲁士王室；要么是舍我其谁的“真命天子”，或是马列理论中最进步的无产阶级（通过它那代表了一切美好事物的先锋队）。<br />
<br />
如何统治与由谁统治<br />
<br />
　　实际上，上述种种主张既无法通过逻辑推演来证明，也难以通过生活实践来检验。因此，当各种不同的人群都纷纷表示自己掌握了推动社会发展的秘诀，因而最适合进行统治时，人们不是通过理性的辨认，而是通过残酷的厮杀来寻找“应该由谁来统治”这一问题的答案。可见，“应该由谁来统治“是一个已经预设了答案的问题，它所带来的必然是强权政治和专制统治。<br />
<br />
　　在一个政治生活主要是围绕“应该由谁来统治“的问题而开展的社会，统治者与被统治者之间泾渭分明。对于不同的政治阵营，获取统治权力都是性命攸关的头等大事。在这样的社会，任何政治势力要想存续，就必须竭尽全力去攫取统治权力，而获得和保持权力的唯一办法便是将政治对手彻底歼灭。<br />
<br />
　　“应该由谁来统治”的问题使政治生活变成你死我活的残酷斗争，不同的政治阵营之间不存在任何妥协的空间。这一问题经常在一个民族的内部人为地制造不共戴天的政治仇恨。存在这种仇恨的社会往往貌似秩序井然，实则四分五裂，一旦遇到外力的冲击总是难逃解体的厄运。<br />
<br />
　　用“应该如何统治”取代“应该由谁来统治”的问题，是人类社会在政治文明方面最主要的进步。提出“应该如何统治”的问题，即意味着所有的人在政治上都是平等的，它不承认一部分人天生具有统治他人的资格。政治制度是人们实行自我治理的机制和手段，人们通过这一机制逐步解决各种社会问题，不断改善自身的生存环境。<br />
<br />
　　人们并不指望某种政治制度可以为所有人带来幸福，因为不同的人对幸福有不同的理解，没有人可以将自己对幸福的定义强加与人。历史一再表明，那些旨在为所有人带来幸福的努力最终只能制造苦难。 <br />
任何政治制度都无法保证人人都幸福，但一种良好的制度至少应该能够尽量避免人为的苦难，并保证每个人都有通过自己的努力追求幸福的自由。这种自由不会被侵害，更不会被剥夺。<br />
<br />
赋予民众必要的政治权利<br />
<br />
　　在政治生活围绕“应该如何统治”的问题而开展的社会，也会有某些人或某个政党获得政治权力并处于领导地位，但他们的权力只能源于被统治者的同意和授权，并且这种权力始终都具有临时性质。被统治者可以通过和平方式摆脱他们不甚满意的统治者，是这种社会最基本的政治特征。<br />
<br />
　　对那些从事政治职业的个人或政党，获得或失去政治权力并非性命攸关的事情。他们要想在政治上有所成就，最主要的事情不是尽量歼灭对手，而是努力制定更为合理的政策，更及时地关注民众的诉求，以及在政治生活中表现得更为廉洁。<br />
<br />
　　政治改革是中国社会目前讨论得较为热烈的话题。很多人都认为政治改革的前提是坚持乃至加强党的领导，好像中国社会的各种问题都是因为这种领导受到了强大的挑战，而不是因为政治权力过于集中。<br />
<br />
　　在中国，最核心的政治问题似乎仍是“应该由谁来统治”，而不是“应该如何统治”。人们最关心的仍是如何维系一个政党的权力，而不是努力赋予民众必要的政治权利，使他们能够通过和平的方式选择及改变统治者。从这一意义而言，中国今天的政治文化和政治制度，和两千多年前的秦朝相比，其实并没有什么实质性的进步。</div>

							
							
							
							
