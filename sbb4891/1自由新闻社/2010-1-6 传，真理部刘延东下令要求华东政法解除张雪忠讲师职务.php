																											<div id="postmessage_418010" class="t_msgfont">张雪忠：军队在国家中的地位<br />
<br />
最近，中国大陆一些重要媒体发表了一些文章，齐声表示要压倒军队国家化思潮。笔者并不想对这些文章中的观点进行评论，因为一篇文章如果逻辑过于混乱，人们想要对其进行批评都会觉得无从下手。本文旨在考察不同的国家体制下军队所处的不同地位，并对中国未来在这方面的发展方向发表一些浅见。<br />
国家是一种由政府垄断暴力之合法使用的社会机制，因此军队在国家中的地位与国家体制的性质密不可分。两者完全可以恰当地比喻为一枚硬币的两面。从军队在国家中的地位出发，可以将国家体制大致区分为三种不同的类型。<br />
第一种是军国主义体制。在这种体制下，国家完全置于军事控制之下，政治、经济、文教等国家生活的各个方面均服务于扩军备战和对外战争。在这种军队高居国家之上的情形下，克劳塞维茨关于战争性质的经典论断被完全颠覆。战争不再是一种政治工具，不再服务于国家的政治利益，国家反而成了为战争提供各种资源的物质手段。<br />
当战争关乎整个国家或民族的存亡时，战争就接近了它的纯粹形态。国家的政治目的和战争目标就会保持一种自然的一致：一切都是为了打垮敌人。此时，军国主义思想和制度最容易得到民众的支持。难怪在二战时期的德国和日本，军国主义者总是一边残忍地蹂躏他国，一边却不遗余力地要让国民相信，他们国家的生存空间受到了致命的外来威胁。<br />
军国主义体制的危险在于，除非被别国彻底地击溃，否则对外侵略便不会停止。将军们在建立战功之后，唯一想做的事情就是再建新功；而在遭受失败时，他们最想要的就是在下一次交战中洗雪耻辱。<br />
第二种是家国主义或党国主义体制。在家国主义体制下，一个特定的家族（以君主为代表）盘踞于国家之上，军队成为家族的私产。军队的主要作用是保卫家族的政权，国家利益只有在不违背家族利益的情况下才能获得一种顺带的保护。辛亥革命前的中国历代王朝，都可以划归为家国主义体制。慈溪太后对待侵略者和国内民众的态度，可以很好地说明这种体制的特点以及军队在其中的地位。<br />
在这一体制中，军队主要被用于监控国内的局势，对外侵略的动机并不大。因此，中国历史上的各个王朝即使是在最强盛的时期，其国防政策仍侧重于防守。家国主义相对军国主义的进步，主要在于确保了文官对军队的控制。<br />
党国主义与家国主义的区别仅在于，居于国家之上的统治集团之成员，不是靠血缘关系而是靠政治认同来识别。四九年之后的中国大致可划归党国体制之列，而之前的国民政府时期则是家国体制与党国体制的奇异混合。<br />
第三种是民国主义体制。在这种体制下，民主政治成为常态，民众成为国家的真正主人，政府由民有、民治、民享。军队既不是将军们满足野心的工具，也不是某个家族或政党的私产，而是捍卫国家利益的公器。在这种军队国家化的状态下，军队不再被用于国内政治斗争，因为只有得到民众授权的个人或组织才有权指挥军队，而赢得政权的唯一途径就是说服民众以获得授权。<br />
在家国或党国体制下，政权的和平传承只有在统治集团内部才有些许可能。否则，不管一种统治是如何让人难以忍受，权力的更替都必须通过血雨腥风的战争才有可能。恰如毛泽东所言：枪杆子里面出政权。<br />
毛泽东的这句话被许多人作为反对军队国家化的论据，这难免让人略感悲哀。这句话的确是对中国历史的恰当写照，但我们并不一定要让它成为预示中国未来的咒语。历史是什么样子，并不能决定未来应该是什么样子。否则，就等于完全否定了中国民众和政治领导人的道德反省能力。伴随着兴亡周期律的同胞之间的相互残杀，并不是中国人无可逃避的永恒宿命。<br />
实际上，毛泽东的论断只是针对非民主的蒋氏政权的有感而发，并不具有跨越时空的普遍性。他在1946年就曾说过：&quot;我们完全赞成军队国家化与废止私人拥有军队，但这两件事的共同前提就是国家民主化。&quot;周恩来也曾表示，如果没有军队的国家化，各种政治力量凡事都要用拳头、用枪炮来&quot;商量&quot;，军队就会成为一种反人民的武装集团，一种披着&quot;国家&quot;外衣的政治土匪。<br />
那些反对军队国家化的人，似乎也在不断强调军队的荣誉，但不知他们是否真的相信，积极参与国内政治斗争真能为军队赢得荣誉。苏联红军的缔造者托洛茨基在国内战争中大显身手，朱可夫元帅则在抗击德军侵略时屡建奇功，而现在的俄国人民难道不是认为朱可夫才是他们真心敬仰的民族英雄吗？<br />
那些在抗击日寇的战争中浴血奋战的人，永远都将作为民族英雄被中国人民所铭记，但国共内战胜负的意义却必然会越来越淡弱。内战所造就的主要是悲剧，而不是功勋。人们难免会怀疑，那些在八九年发挥作用的军人，内心是否真的产生过一丝一毫的荣誉感。实际上，人们至今都不曾看到，有谁会公开承认在其中起到过重要作用。<br />
笔者注意到，那些在主要媒体撰文反对军队国家化的人，大多具有军人身份。对军人由来已久的尊敬使笔者不忍对他们多加批评，但笔者的确想以一个同胞的身份提醒他们：军队赢得荣誉的最佳途径是在战场上击败敌国的军队，而不是时刻准备把坦克开进本国首都的广场。</div>

							
							
							
							
