																											<div id="postmessage_418121" class="t_msgfont">张雪忠：中国需要去马克思主义化<br />
<br />
　　张雪忠（中国华东政法大学教授）<br />
<br />
　　在中国大陆，官方一直强调应该坚持马克思主义在意识形态领域的指导地位，反对搞指导思想的多元化。而马克思主义哲学，在中国也一直被视为将唯物主义和辩证法有机统一起来的科学世界观。<br />
<br />
　　谈到科学，人们总是指关于“世界是什么样子”以及“世界为什么是其所是”的各种知识体系。而关于“世界应该成为什么样子”，以及“人们应如何改变世界”的陈述，它们本身并不能提供知识，而是为了提供包含道德评价的社会行动纲领。<br />
<br />
　　当马克思宣称“哲学家们只是用不同的方式解释世界，而问题在于改变世界”时，就等于告诉人们，他感兴趣的与其说是知识问题，不如说是政策问题。对于认识的来源与途径这一根本性的哲学问题，马克思主义只不过是利用含糊其辞的“实践”概念复活了唯物主义的独断论。<br />
<br />
　　马克思主义认为，人们可以通过实践超越认识对感觉经验的依赖，实现“主体与客体的同一”。但实践其实并不是一种独立于视觉、听觉、味觉、嗅觉及触觉之外的神秘的第六感。就其认识作用而言，实践只不过是五种通常的感知方式的综合运用而已，实践并不包含比感官知觉更高级的认知途径或认知方式。实践独特作用在于实现人的自由，而不是提供高于经验的知识，尽管它有助于扩展人类经验的范围。<br />
<br />
　　在物质与意识的关系上，马克思主义认为，物质是独立于意识的客观存在；意识则是高度发展的物质（人脑）的机能，是客观物质世界在人脑中的反映。一方面把物质定义为“独立于意识的客观存在”，另一方面又将意识定义为“物质在物质中的反映”，这种循环定义恰恰是哲学独断论的典型特征。<br />
<br />
　　如果意识只是物质与物质相互作用的产物，人们难免要问：意识是否也属于物质？如果是，马克思主义怎能用一种特殊的物质，来定义普遍的物质呢？如果不是，那么物质与物质的相互作用，如何能产生非物质的东西呢？如果毕竟存在非物质的东西，那么“存在”怎么会仅仅是物质的本质属性呢？<br />
<br />
　　马克思对待宗教的态度很能说明其哲学的独断性质。他宣称要“废除作为人民的虚幻幸福的宗教，并要求人民的现实幸福”。宗教确实曾被用于维护绝对的政治权力，并经常成为暴政和社会压迫的帮凶。但完全消灭宗教并将幸福视为纯粹现实和客观的东西，则等于在将旧神学从政治生活中驱除出去的同时，又迎来了一种更具压迫性的新神学。<br />
<br />
　　一旦幸福被视为完全客观的东西，人们就必须承认，有些人比其他人更能“认识”到什么是幸福，从而应该掌握绝对的政治权力。据马克思看来，在摧毁了真理的彼岸世界之后，“历史的任务就是确立此岸世界的真理”。此岸之神急不可耐地要取代彼岸之神的位置！“先进阶级论”作为一种政治哲学的返祖现象，使人不免想起亚里士多德在《政治学》的高论：人之所以必须分为主人和奴隶，就在于主人对双方的共同利益有着特别的洞察力。<br />
<br />
　　马克思哲学理论中强烈的道德义愤，并不特别有利于使其成为真正的科学，马克思提出的各种社会政治主张，也不会因此就具备等同于客观规律的不可易变的必然性。但正是凭借着这种臆想的必然性，许多马克思主义者固执地认为，马克思的社会政治主张不是解决现实问题的可能途径之一，而是解决现实问题的唯一可能的途径。<br />
<br />
　　这种臆想的必然性往往经不起辩驳。例如，要避免宗教成为暴政的借口，并不必然要求彻底消灭宗教。洛克曾对绝对君权的神学基础进行最为系统和彻底的批判，但他并不认为避免暴政就一定要消灭宗教。通过将宗教信仰私人化，既可以涤除政治中的神学因素，又可以为人们保留据以获得心灵慰籍和追求道德完善的依归。<br />
<br />
　　马克思主义的历史唯物主义断言，生产力决定生产关系，资本主义社会高度发达的生产力，为人类社会迈入共产主义准备了物质条件。但此处的生产力概念，显然是源于对物理学中力的概念的错误理解。生产力和力一样，都只不过是一个描述性的概念，并不代表什么实在性的东西。<br />
<br />
　　生产力并不是比生产关系更基础的东西，更不是被生产关系的“桎梏”所包裹着的一个蠢蠢欲动的胎儿。它只不过是一个描述性的概念，用于测度生产工具、生产对象以及生产关系这些要素组合的预期产量。生产力作为一个因变量，不是上述组合的基础，而是上述组合的函数。<br />
<br />
　　即使把生产力本身视为某种实在的东西，在人们既看不到起点也看不到终点的人类历史中，如果能找到多少生产力的变化带来生产关系变化的史例，就一定能找到同样多的生产关系变化带来生产力变化的史例（大跃进及文革后的情况都是很好的例子）。在这种情况，只有足够独断的人，才会宣称一定是生产力决定生产关系。比如，一团密闭的气体，如果温度升高，其压力就会增加；反之，如果其压力增加，温度就会升高。谁能说到底是温度决定压力，还是压力决定温度呢？<br />
<br />
　　马克思主义认为，资本主义社会化大生产和生产资料私有制之间的矛盾，只有经过社会革命才能克服。但这一矛盾其实早已被人们通过企业制度的创新（如股份公司制度）予以解决。由于对革命的强烈嗜好，马克思主义者显然不愿承认，许多社会问题完全可以通过技术手段予以解决。<br />
<br />
　　实际上，与其摧毁私有制和市场经济，以追求超越任何经验知识的共产主义，人们不如保留曾使社会财富不断涌现的私有制和市场经济，同时让劳工阶层参与以普选权为基础的议会政治，以制定合适的税收与福利政策，防止出现严重的贫富差距。这一途径有一种显而易见的优势：人们不用冒孤注一掷的风险，只需通过一种逐步试错和灵活调整的过程，就有可能实现一种明智和人道的社会主义。<br />
<br />
　　其实，包括共产主义思想在内的任何超验的理论观点，只要停留在超验的领域，都不会对人有害。但一种毫无经验基础的观念一旦通过强权予以实践，则必然会破坏人们的生活与福利。如果不可检验的东西获得了现实的权力，以致要控制和左右其他可检验的东西，那么最可怕的人类惨剧便会发生。活人殉葬、宗教裁判以及强制集体化，都是超验观念控制现实生活的显著例子。<br />
<br />
