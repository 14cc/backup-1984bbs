<br />
目前的中国有一种非常值得注意的现象，即人们经常只是因为在某些社会问题上看法不一，便轻易的以爱国来标榜自己，以卖国来指责别人。 <br />
<br />
为什么有些中国人总是想当然地认为自己比别人更爱国？这往往缘于爱国的情感衍生的两个有害的变种：“伪善型爱国主义”和“虚狂型爱国主义”。 <br />
<br />
伪善型爱国主义者喜欢打着“爱国”的旗，干着营私的勾当。例如，那些一边高喊爱国的口号，一边却把有毒的食品卖给同胞，还有大权在握却大肆贪赃并将钱财和家人一齐送至国外的人。 <br />
<br />
虚狂型爱国主义喜欢把国家概念轻易人格化。国家这一词语，本来只是被用来笼统地指代广泛的不具有人格的各种事物，比如某一地理疆域以及在其中发生的语言、文化和社会制度等。基于一种想象中的道德优势，虚狂型爱国主义者尽管自己实际上不可能完全做到，也总是会要求别人不问是非且不计利害地热爱和偏袒一个国家的各个方面，像子女“无条件”爱自己的母亲一样。那些对政府进行批评的人，那些对本国历史或文化进行反思的人，那些指出国内各种问题的人，在虚狂型爱国主义者眼里，都是不够爱国的。 <br />
<br />
真正的爱国者从不回避本国存在的问题，而且他们会把爱落实到同胞身上。因为他们知道，在他们所热爱的与祖国有关的各种不同的对象中，唯一具有人格因而配得上无条件挚爱的，只有生活在同一片国土上的一个个活生生的人。</div>

							
							
							
							
