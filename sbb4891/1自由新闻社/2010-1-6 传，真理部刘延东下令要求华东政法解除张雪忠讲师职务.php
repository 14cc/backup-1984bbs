																											<div id="postmessage_418432" class="t_msgfont">张雪忠（上海）<br />
（2008-06-18联合早报）<br />
<a href="http://origin-" target="_blank">http://origin-[url]www.zaobao.com/yl/tx080618_502.shtml</a>[/url]<br />
5.12大地震给四川民众造成了重大的生命与财产损失。地震发生后，中国温家宝在第一时间赶赴灾区。他在救灾时的坚毅、镇定，以及与民同哀的情感流露得到了世人的赞许，也让很多人深为感动。<br />
<br />
实际上，温家宝早已以其一贯的勤勉、亲民和务实表现，在中国赢得了较高的民意认同。就个人人格而言，有很多人会认为温家宝是一个值得尊敬的人，但在中国目前的制度背景下，作为中国行政体系的最高领导人，温家宝又是一个极富悲剧性的人物。<br />
<br />
尽管中国可能有一位亲民、勤勉和务实的总理，但政府体系中的腐败仍积重难返；各种人为的生产安全和产品质量事故仍层出不穷；政府官员利用行政权力侵犯民众权益的恶性事件仍时有发生。当一个大国的总理以疲于奔命的“救火员”的形象被人们所牢记时，人们必须承认，中国面临的是结构性而不是个案式的问题。<br />
<br />
人们可以看到，温家宝在不断地以总理之身亲临现场，对全国各地的突发事件进行个案式的介入。但是，面对政府体系中一目了然的结构性顽疾，他却无计可施。而政府系统中的种种结构性顽疾，才是各种事件频频发生的根源。<br />
<br />
此次汶川地震暴露了一个让人良知难安的事实：中国可以建好“鸟巢”、“水立方”，也可以修建很多豪华气派的政府办公楼，但却修建不了结实安全的中小学校舍。<br />
<br />
相对一个被人称赞的好总理，那些曾经鲜活的小生命更需要一个关心校舍安全的校长、一个不克扣校舍建设费用的教育局长、一个不在校舍施工中偷工减料的建筑商、一个不容易被人打点的建筑监理师。但让人遗憾的是，目前的政治制度并不能保证这些最基本的需要能够得到满足。<br />
<br />
作为中国最高行政机构的领导人，无法对这个弊病丛生的行政体系加以革新，而只能不断去面对、处理这一体系所滋生的种种不良后果，这无疑是温家宝作为中国总理让人深感悲悯之所在。<br />
<br />
这种悲剧性情状在中国现行制度下几乎不可避免。不管温家宝表面上是多么位高权重，他的权力基础仍是中国庞大的党政官僚体系。如果没有新的独立于官僚体系的政治权威来源，人们不可能指望政治领导人在大刀阔斧地革新政府体制的同时，又能稳固自身的政治领导地位。<br />
<br />
温家宝在中国民众中具有极高的民意认同，但由于民主政治的缺乏，这种民意认同无法通过定期的民选机制（这种民选机制既是授权机制也是权力约束机制），转化为用于推动政府体制改革的政治权威。<br />
<br />
民意支持和领导人的政治权威之间缺乏必要的制度联系，这使得温家宝不可能像其他国家（以及我国台湾地区）的民选领导人那样，在政治上担负决策责任的前提下，利用官僚体系充分贯彻自己的决策意志。<br />
<br />
官僚体系即使是阻碍或扭曲政策的执行，政治领导人对此也毫无办法。自上世纪八十年代以来，中国已经进行了多次行政体制改革，最终都无功而返。这样的结果毫不令人意外。如果不进行改变当前权力结构的政治改革，任何行政改革都只不过是隔靴搔痒而已。<br />
<br />
人们可以看到，即使是亲临突发事件的现场，温家宝作为总理的决策意志也往往难以得到充分的贯彻：那些被免职的官员在其离开后，可能很快又东山再起；那些已发到慰问对象手上的红包也可能又被悉数收回。庞大的官僚体系就像一座无法翻越的大山，始终横亘在广大民众与那些可能还有心为民谋利的政治人物之间。<br />
<br />
在中国，各级政府领导人的政治权力均来自党政官僚体系的认同，而不是源于选民的授权。只要能维护官僚体系的利益，他们的权力往往大到足以作恶。如果他们的决策会损害官僚体系的利益，他们的权力却又小到不足以行善。<br />
<br />
不管温家宝有多么亲民、务实和勤勉，他都无法对庞大的政府体系进行必要甚至迫在眉睫的改革，因而也不可能实质性地降低腐败的政府体系对国家和民众的危害。相反，这个弊病丛生的官僚体系，却可以不断利用他个人展现的亲民、务实和勤勉，赢得一些似是而非的“合法性”，并因此得到进一步的巩固。这可能是温家宝作为中国总理最具悲剧性的作用之所在。</div>

							
							
							
							
