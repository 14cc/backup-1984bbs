<br />
联合早报网3月10日刊文报道： <br />
<br />
不少人对中国即将开展的行政改革寄予厚望，并期盼中国的政府系统能变得精简、公正、透明、廉洁、高效。他们最终很可能会大失所望。自1982以来，中国几乎每五年都要进行一次声势浩大的政府机构改革，而每一次改革的目标都大致相同。<br />
<br />
不可能完成的任务 <br />
每一次机构精简，国家都要付出巨大的代价，向“被精简者”进行现金或其他形式的补偿，但这些人腾出的位置很快又被更多的新进人员填满。1995年，中国行政事业支出在财政总支出中所占比重是11％，到2006年却达到19％―20％。另外，经过多次机构改革后，中国的政府系统似乎仍不以公正、透明、廉洁和高效而著称。 <br />
<br />
中共十七届二中全会会议公报对“巩固党的执政地位”的强调表明，此次的行政管理体制改革承载的，仍是一项不可能完成的任务：在维持单个政党对政治权力全面垄断的同时，政府系统将变得高效、廉洁，甚至成为“服务型政府”。如果政治学的一些重要原则在中国也能适用的话，那么让一个无需问责的政府自我革新，并不会比让一个人抓住头发把自己拽离地面更为容易。 <br />
<br />
如果政治权力在公民中的分配是不平等的，甚至被某一政治集团所垄断，便会产生掠夺型国家，并出现泾渭分明的统治者与被统治者。在这样的国家，统治者总是竭力寻求“租金”最大化，国家的经济活动和政治生活也常常围绕这一目标而开展。 <br />
<br />
政治权力分配不平等<br />
在政治权力被垄断的国家，那些有权制定和执行政策的人，总是使政策符合他们自己的利益，而不是社会的整体利益。由于他们无需面对实质性的政治压力，因而不可能有革新现行制度的动力和欲望，甚至会极力抵制这种制度革新。 <br />
<br />
相反，中国的政府系统尽管弊病丛生，那些显然必要的改革却无法推行。历次行政体制和政府机构改革最后都是雷声大、雨点小，因为这些改革所针对的，恰恰是政治影响最大的利益集团。 <br />
<br />
面对再次来临的行政管理体制改革，那些乐观的人应该想想，为什么前五次未能成功的事业，这一次一定会成功呢？如果没有民主政治改革的前提，如果不改变政治权力分配状况和政治动力结构，任何行政体制和政府机构改革都必将无疾而终。</div>

							
							
							
							
