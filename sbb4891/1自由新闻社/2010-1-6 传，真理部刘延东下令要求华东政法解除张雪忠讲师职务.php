最近，中国全国政协主席贾庆林在《求是》杂志上撰文表示，人民政协在思想建设中，要“筑牢抵御西方两党制、多党制、两院制和三权鼎立等各种错误思想干扰的防线”。随后，中共党报《人民日报》在理论版以专题形式刊发了三位学者的文章，论述中国“为什么要坚持人民代表大会制度而不能搞‘三权分立’”。<br />
<br />
三权分立制度的理论基础可以溯源至洛克和孟德斯鸠等人提出的分权学说。这一学说基于一个经验性法则，即不受约束的权力必然导致暴政和腐败。根据这一学说，国家权力应该分立为立法、行政和司法等三种权力，并使它们互相制衡。<br />
<br />
分权与制衡原则的基本政治功能，是防止专制并保障公民的自由。三权分立制度与代议制民主的结合，在确保公民普遍参与国家政治生活的同时，也可以防止国家权力的独断与暴虐。这实际上是现代宪政民主的基本政治架构。<br />
<br />
在上述三位学者看来，人大制度相对于三权分立的主要优越性之一是，它有很强的组织动员能力和社会掌控能力，有利于集聚国家力量办成大事。但这与其说是人大制度的优越之处，不如说是它秉持了与三权分立不同的制度价值。<br />
<br />
三权分立的主要制度功能，便是对国家权力支配社会资源的范围和方式进行必要的限制。而人大制度作为一种集权政体，恰恰是为了使政府获得不受约束的资源支配权力，在贯彻政府意志和实现政策目标时，民众意愿和机会成本从来都不是重要的考虑因素。<br />
<br />
在任何社会都存在资源稀缺问题的前提下，让政府获得不计代价办成一些由政府决定的大事的权力，这并不像上述学者所说的那样，代表一种政治制度的民主优越性。因为民主政治的核心问题恰恰在于，民众是否可以决定政策目标以及为实现这些目标所愿意支付的成本。<br />
<br />
衡量政府效率的更合理标准，是政府在严格预算约束条件下实现一定政策目标的能力。从这一角度而言，中国政府在效率方面并无任何值得自豪之处。本次北京奥运会据说举办得极为成功，但政府对其实际成本却一直讳莫如深。在这一问题上，北京奥运会开幕式导演张艺谋可谓一语中的：“其实西方人也想搞‘人海战术’，可是他们找不到这么多人，也花不起这些钱啊。”<br />
<br />
人大制度与三权分立孰优孰劣，这应该是可以通过经验事实来加以回答的政治科学问题。在实现民主的作用上，人们可以问：哪一种制度更能为民众提供政治参与空间，更能强化政府问责机制？在政府效率上，人们可以问：哪一种制度可以同等资源为民众带来更优良的公共服务？人们还可以进一步追问，哪一种制度更能遏制腐败，哪一种制度可以提供司法公正，哪一种制度可以确保公民免受权势的凌辱，等等。<br />
<br />
那些赞扬人民代表大会制度的人，并不愿意面对这些可以从经验事实中找到答案的问题。他们认为，人大制度可以贯彻共产党的领导，而共产党又是由马克思主义武装起来的，因而是人类历史上最具进步性的政党，可以领导民众进入最美好的人类历史阶段。这样一来，这些人就把属于经验世界的政治科学辩论变成了一种超验的、带有神学色彩的诡辩。<br />
<br />
实际上，任何人都不可能拥有关于人类历史的完整和详尽的知识。人们对5000年前的人类社会的情况显然知之甚少，而即使是对100年后人类社会的发展，人们又能作出多少可靠的预测呢？声称一个政党是人类历史上最先进的政治力量，因而天然地拥有政治领导权，这种断言并不比早被抛入历史垃圾堆的“君权神授”更为科学和合理。<br />
<br />
那些提出这种超验主张的人，如果不是出于私利的驱使，便是出于理性的怠惰。正是依恃此类超验主张，一种制度尽管实际剥夺了公民的政治参与权利，却被称为最具民主性的制度；一个腐败无孔不入的政府，却被说成最能为民谋利的政府；依仗权力视民众如草芥的官员被称为公仆，如此等等。<br />
<br />
但是，判断一种政治制度是否公正合理的终极标准，决不是那些声称洞察了人类历史客观真理的形而上学体系，而是民众内心的主观感受。根据自身的感受，对影响自身生活的政治制度进行评判，并决定是否要对其进行变更，在任何社会都是民众的自然权利。在这里，‘自然’一词是在其最真实的意义上被使用的，因为民众的这一权利在客观上既无法放弃也无法剥夺。<br />
<br />
正是基于自己作为一名普通民众的个人感受，笔者相信人民代表大会这种“代议”机构的最终命运，很可能就像国民政府的“万年国代”一样，只是作为一个笑柄而载入中国政治实践的史册。它唯一正面的作用就是用以警示世人，一种不合理的政治制度可能造成的后果，是多么的荒诞和可悲。</div>

							
							
							
							
