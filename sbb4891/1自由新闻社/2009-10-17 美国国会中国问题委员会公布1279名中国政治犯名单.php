													<h2>美国官方机构发表中国人权年度报告</h2>
						
						
																											<div id="postmessage_287596" class="t_msgfont">VOA<br />
<br />
美国国会及行政当局中国委员会针对中国人权与法治发表年度报告，报告中对中国当局侵害人权以及法律改革迟缓等问题表示严重关注。<br />
<br />
*1279名中国政治犯简介*<br />
<br />
这个委员会是在星期五发布这份报告，报告中还附加一份文件，介绍了目前遭到中国政府拘押或监禁的1279名政治犯。委员会主席，参议员拜伦・多根以及委员会副主席，众议员桑德・莱文在联合声明中表示，中国政府持续违反人权，同时延宕法律改革的作为令人担忧。声明说，中国政府为了平息骚乱，巩固政权所设定的许多政策，导致中国公民人权的严重受损。<br />
<br />
两位议员的声明还说，中国政府将经济发展作为首要任务，确实让数百万中国民众脱离贫困。但是中国政府的政策和做法持续违反人权以及国际标准。<br />
<br />
*西藏新疆及维权人士*<br />
<br />
报告中批评了中国当局在处理西藏，新疆骚乱时的做法以及持续打压零八宪章签署人，打压揭发川震豆腐渣工程的维权人士以及揭发政府官员腐败行为的中国公民。<br />
<br />
报告指出，中国政府将这些人士视为对中国安全的威胁，并持续监控新闻媒体。此外对因特网言论也设定重重限制。<br />
<br />
委员会主席多根在声明中表示，为了确保美中关系的正面发展，中国领导人必须展现真诚的的许诺，不仅仅是文字，还要付诸行动，把人权发展放在与经济发展同等重要的地位。<br />
<br />
*北京反驳*<br />
<br />
中国外交部发言人答记者问时说：这个所谓的委员会一再发表歪曲事实、干涉中国内政的年度报告，其言论充满无知和偏见。发言人马朝旭奉劝该委员会停止干涉中国内政和破坏中美关系。<br />
<br />
这反应了北京当局对该年度报告的一贯立场。过去，北京还要求美国采取切实措施消除这个报告带来的影响。<br />
<br />
*委员会由议员和官员组成*<br />
<br />
美国国会及行政当局中国委员会是在2000年10月成立，专门研究和监督中国的人权状况和法治发展，每年向国会和总统就中国在这些领域的发展提交年度报告。<br />
<br />
委员会由9名众议员，9名参议员以及5名资深行政官员组成，通过听证会，非正式会议以及前往中国搜集资料等作为年度报告的参考依据。</div>

							
							
							
							
