													<h2>从“秋后算账”到“五日履新”</h2>
						
						
																											<div id="postmessage_203469" class="t_msgfont">　羽戈 <br />
<br />
　　<br />
<br />
　　一上网就看到两条焦点新闻，上下陈列，颇具喜感。上一条讲的是郑州大学第一附属医院在不具有职业病诊断资格的情况之下进行职业病诊断，违反了《职业病防治法》，被河南省卫生厅通报批评，并被立案调查。这家医院是什么来头，怎么捅了马蜂窝呢？前不久，有民工张海超要求“开胸验肺”，寻求其病情的真相，就是在郑大一附院进行的手术，他被诊断出“尘肺合并感染”。随后，卫生部督导组介入，郑州市职业病防治所确诊张海超为尘肺三期，这一出充满悲情的闹剧才算了结。殊不知，张海超历经艰辛讨到了“说法”，为他证明真相的郑大一附院却遭遇了非典型的“秋后算账”。<br />
<br />
　　另一条新闻可称之为“五日履新”。话说7月底，身为泸州市龙马潭区交通局局长的谢林，因两年不交小区停车费，与物管人员发生冲突，大打出手，揍了人家一拳。不过谢局长并不好受，此事经媒体曝光，天下皆惊。8月5日，谢林的交通局党委书记与局长之职皆被免除――说实话，这个处罚重了一些，骂人打人至多算私德有亏，官职任免则属公权力的自我调整，两者相挂钩，很容易导致公权与私权沆瀣一气，鱼肉不分。可两天后，被免职的谢林便去泸州市经济开发区管理中心报到；10日，正式任职，职务是办公室副主任。原来，五日前打了你一耳光，五日后再喂你一颗大白兔奶糖，真难为了泸州市的主政者，对待下属犹如大人哄小孩。<br />
<br />
　　这两个新闻对照起来阅读，便可窥见当今某些执政者的治理逻辑。为什么郑大一附院不能“五日履新”呢，譬如在对张海超的“开胸验肺”手术成功以后，不仅不去抓该医院的小辫子，反而顺理成章授予其职业病诊断资格？为什么不对谢林“秋后算账”呢，他既然丢掉了交通局长的乌纱帽，正适合追索他是否存在滥用职权等恶行？当然，这样的设问在短时间内并无实现的可能，否则改革真成了坐过山车。“为什么”的背后，潜藏了民工张海超与局长谢林所共同依附的体制深刻的悖谬。<br />
<br />
　　关于“秋后算账”，再说一条更名副其实的新闻。2007年4月，河北魏县政府修建北环路，占用了张建军一家仅有的两亩多土地和一眼机井，因补偿太少，张家不满，最后走上了上访之路。2008年7月（奥运前夕，魏县政府自然要委曲求全），魏县领导通过中间人（并不直接出面，为翻案留下余地）与张建军协商，决定赔偿机井损失2万元。拿到钱后，张家以为此事终于了结，哪知今年3月（大局已定，算账开始），张建军被捕，罪名是敲诈勒索。7月13日，魏县人民法院一审判处张建军有期徒刑4年，并没收“非法所得”2万元。<br />
<br />
　　敲诈勒索罪的一大构成要件，即要求犯罪行为人必须具有非法强索他人财物的目的，并采用了威胁、要挟、恫吓等手段。张建军为自家被侵占的土地和机井索取合理的赔偿，正如让债务人欠债还钱，哪里“非法”呢；一眼机井索赔2万元，不能算高――我查阅作于2008年的《关于河北省节水灌溉的调研报告》，其中说“打一眼机井需要10多万元”――自然不存在“敲诈”之嫌。所以说，这份判决完全就是报复，与法理无涉。<br />
<br />
　　“秋后算账”有两种做法，其一是挖空心思，费劲脑汁，找你身上的法律缺口，然后安插一个让你有苦说不出的罪名，如郑大一附院的遭遇；另一种则简单多了，古人还说，匹夫无罪，怀璧其罪，今人哪问你怀璧与否，只管无中生有，株连罗织。在有些人眼里，带刀就意味着要犯罪，张建军被判敲诈勒索罪，乃是这种算账法的代表作。两者相比，后者无疑更为流行。<br />
<br />
　　不管是哪一种，将造成法律之结局，或者沦为纯粹招牌，或者被弃若敝屣。由此而论，“秋后算账”更像是无法无天的江湖世界的潜规则，今日容忍胯下之耻，明日一箫一剑快意恩仇。它如果在现代国家过于盛行，那只能说明，法治仍未昌明，至少在主张“秋后算账”的某些施政者眼中，法律轻贱如厕纸，用过即扔，他们认同权变，认同成王败寇――这是法治的天敌。<br />
<br />
　　损害了法律的尊严，损害了政府的公信力，“秋后算账”者同时将自身置于了人尽可辱的绝境。按照它的运行逻辑，终有一天，有一笔账会算到它头上，连本加利，罄竹难书。<br />
<br />
　　<strong>一面是“秋后算账”，一面是“五日履新”，一面以手中的权力与法律为敌，与公众为敌，一面是权力者相互提携，这两面相辅相成，撕裂出了一道道触目惊心的伤口――如果不尽快医治，任其蔓延，有多少人可以对未来保持乐观？<br />
</strong><br />
　　（作者系青年学者）<br />
