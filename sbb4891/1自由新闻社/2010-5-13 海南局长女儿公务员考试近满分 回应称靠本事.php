<br />
　　13日下午，有网友向记者举报，在5月11日三亚市人力资源和社会保障局网站公布的《三亚市小额贷款担保中心招聘人员笔试成绩及入围面试人员》名单中，除了三亚市社保局局长温孝廉女儿温娉婷以99分(满分100分)的绝对高分位列榜首外，名列前三甲的考生均系琼海市社保局局长和文昌市社保局长的子女。<br />
