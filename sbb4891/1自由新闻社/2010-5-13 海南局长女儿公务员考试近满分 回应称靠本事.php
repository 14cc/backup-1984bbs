<img src="http://img.hexun.com/2010-05-13/123687403.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
三亚市人力资源和社会保障局网站公布的笔试成绩及入围名单<br />
<br />
<img src="http://img.hexun.com/2010-05-13/123687404.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
报名表中无温娉婷考生名字<br />
<br />
　　南海网5月13日报道近日，南海网网友报料称：三亚市小额贷款担保中心招考工作人员过程中，一名叫温娉婷的考生行政职业能力测试成绩，以让人大跌眼镜的分数――99分(100分为满分)入围该中心综合岗位，并对其成绩的真实性和身份产生了怀疑。<br />
　　5月12日，记者专门就此事采访了三亚市社保局，并证实该考生温娉婷确系三亚市社保局局长温孝廉的女儿，局长温孝廉针对网友质疑一一做出了回应。<br />
<br />
　　质疑一：高难度行政能力测试何以考出99分?<br />
　　局长温孝廉：99分是女儿凭自己本事考取的<br />
<br />
　　网友发帖称，此次行政职业能力测试题型是按公务员考试题型设置的，共100道题，满分为100分。他分析道，一套试卷最高分有考99分的，那么前十名当中定有一两个是90几分的，就算一个都没有也应当有80来分的，可惜在300多名考生中只有温娉婷一个人考了99分，第二名只有79分，而且还是其他岗位的考生。网友质疑，这位99分的牛人，是IQ、EQ异于常人呢，还是运气太好?又或者是这其中有什么“奥秘”?<br />
<br />
　　记者采访温孝廉局长时，局长温孝廉称，此次考试是由三亚市人力资源和社会保障局组织的，三亚市社保局只不过是三亚市人力资源和社会保障局的一个下属二级局，因此，自己不可能干预此次考试，也不可能是自己一个人说了算。温孝廉局长表示，女儿此次行政职业能力测试能考99分，完全是凭自己的实力和本事。<br />
<br />
　　质疑二：没有参加报名审查何以参加最终考试?<br />
　　局长温孝廉：之前不了解内部优惠政策所以没有报名<br />
<br />
　　网友发贴表示，在三亚人事局4月21日公布的报名审查合格考生中，根本没有温娉婷这个名字，于是再次发出质疑：一个连名都没报，审核都没参加的人，又怎么能够参加这次考试?<br />
　　局长温孝廉表示，因为之前不了解有关优惠政策，考虑到女儿不符合公布的报名条件，所以没有参加最初的报名和报名审核。<br />
<br />
　　质疑三：还未大学毕业如何提供报到证及学历专业证书<br />
　　局长温孝廉：系统内部职工子女可以放宽报名条件<br />
<br />
　　该网友还提出，在这次招聘明确规定：报名者必须提供本人身份证、户口本、报到证(或派遣证、介绍信)、学历专业证书原件及复印件……网友质疑，考生温娉婷是长春理工大学2006届学生，一个要到今年7月份才毕业的大学生，又怎么能够提供报到证、学历专业证书原件及复印件?<br />
<br />
　　局长温孝廉解释，这是因为后来了解到系统内部有规定，今年大学毕业暂时不能提供报到证、学历专业证书的内部系统职工子女，也可以参加此次考试，所以后来就又报名了。<br />
<br />
