																											<div id="postmessage_535813" class="t_msgfont">深圳男子驾车被警察击毙 家属围堵派出所<br />
2010-03-27 11:05:19　来源: 华商网(西安)　 手机看新闻 <br />
核心提示：25日凌晨，深圳男子张水强被警方击毙。警方称对方涉嫌盗窃车辆，疯狂撞击警车，警察鸣枪示警无效后才将其击毙。26日上午，张水强的数十名亲属围堵坪山公安分局宝山派出所讨说法。<br />
<br />
<br />
<br />
<br />
昨日上午10时许，张水强的数十名亲属围堵坪山公安分局宝山派出所讨说法。<br />
<br />
华商网3月27日报道&nbsp; &nbsp;“宝山派出所大门被数十男女堵住了，民警、巡防队员站在大门内对峙”，昨日上午10时许，市民胡先生报料。记者赶到现场看到，警方在宝山派出所门前拉起封锁线，数十名男女在派出所大门前或蹲或站，打出横幅喊起号子讨说法。<br />
<br />
“我老公被民警击毙了，本来是出去吃夜宵的，却再没回来”，女子陈丽婷抱着4岁的女儿说，她老公张水强今年29岁，张水强和姐姐在澳子头合开一间发廊，为节约成本，张水强自己也做理发师。前日凌晨，张水强和亲戚张孟钦一起去金碧路金田百货前大排档吃夜宵，但再也没回来。<br />
<br />
“出去吃夜宵，然后就生不见人死不见尸”，陈丽婷说，与张水强一起出门的是亲戚张孟钦，到前日下午，张水强、张孟钦也没回家，后来就接到警方通知称“张水强无证驾驶套牌报废车拒查冲卡，撞击警车逃跑，民警鸣枪示警无效，张水强被击毙”，张孟钦也被警方控制，家属也见不到面，所以没法了解到更多情况。<br />
<br />
张水强的姐夫刘左生说，四处打听才知道，张水强吃完夜宵上车打不着火，民警怀疑是赃车上前检查，张水强心慌，打着火后开车跑，民警驾车追了3公里左右，鸣枪示警了，但张水强没停车，民警便开枪打中了张水强头部。<br />
<br />
张水强的堂哥张足浪说，张水强很老实的，说他开车逃跑有可能，但说他开车撞人绝对不可能。但是家属承认，张水强开的确实是一辆报废车，张水强正在学车，2个月前在坪山买了一辆二手微型面包车，没有执照，买车就是为了学车的，车子是报废车，价格很便宜。<br />
<br />
“我们打听到，民警没穿制服，是便衣，张水强遇到便衣拦截肯定心慌”，张足浪说，警方说民警鸣枪示警了，但张水强的车很差，发动机响得很，坐在车里肯定听不见。昨日，记者赶到金田百货，看到超市前的烧烤摊等已经收起来了，附近店铺经营者说，他们关门早，凌晨的事听说了，但都没有看到。<br />
<br />
深圳警方介绍，前日凌晨1时许，在坪山警方开展“平安鹏城10”专项行动中，两名涉嫌盗抢车辆人员驾驶嫌疑车辆冲卡逃跑后又多次驾车疯狂撞击警方追捕车辆。在鸣枪警告无效，生命安全受到严重威胁的情况下，民警依法果断开枪将驾车犯罪嫌疑人击伤，当场抓获车上的两名犯罪嫌疑人张某某、张某。被击伤嫌疑人张某某送医院抢救无效死亡。<br />
<br />
警方介绍，前日凌晨1时许，根据“平安鹏城10”专项行动统一部署，坪山公安分局宝山派出所民警带领巡防队员在坪山新区碧岭社区附近设卡盘查时，发现路边一辆白色小面包车(车牌号为粤SK×××2)，民警迅速通过查询系统核查该车牌为假牌。民警立即驾车拦住嫌疑车辆去路，并下车向两名男子表明身份，要求二人下车接受盘查。两名男子突然加大油门，突然驾驶车辆疯狂撞向民警。在民警闪身躲开后，嫌疑人驾车撞开警方的拦截车辆，朝金碧路方向逃窜，民警迅速驾车对逃窜嫌疑车辆实施追捕。<br />
<br />
<br />
嫌疑车辆行至碧岭工业区物业小区出口时，撞断了小区出口岗亭的大门横栏杆，然后强行转向锦龙大道方向逃窜。警方追捕车辆追上嫌疑车辆后与之并行，追捕民警再次表明身份，警告嫌疑车辆立即停车接受检查。嫌疑车辆不仅未减速停下，反而再次用车头撞击警方追捕车辆。车辆行至横坪公路横岗方向时，嫌疑车辆突然掉转车头逆行，再一次撞击警方追捕车辆并造成警方追捕车辆副驾位车门严重凹陷变形。随后，嫌疑车辆驾车后退，第三次加速撞击警方追捕车辆。坐在副驾驶位的民警立即鸣枪警告，但嫌疑车辆无视警告继续疯狂撞击警方追捕车辆。在生命安全遭受严重威胁、鸣枪示警无效的情况下，民警果断向嫌疑车辆驾驶位开枪，嫌疑车辆疯狂撞击行为才停止。民警迅速上前对车内嫌疑人进行抓捕，在嫌疑车内抓获两名犯罪嫌疑人，其中一人已被警方击伤。在控制现场后，民警立即拨打120，通知120到场救治。同时将情况向分局、派出所进行了报告，被击伤的嫌疑人经120送至医院后，抢救无效死亡。<br />
<br />
经初步审查，两名嫌疑人驾驶的嫌疑车辆发动机号码和车架号码都被篡改切割，车牌系假牌，车辆系被盗抢车辆。目前，龙岗区检察院已介入调查。</div>

							
							
							
							
