川东名胜古刹重庆北温泉寺简介<br />
　　（据巴县志卷廿22-23页及温泉寺古碑文考） <br />
　　渝郡西去百里许，曰宝峰，即缙云山。山之东麓下是嘉陵江温汤峡，峡畔有泉，名大蟒泉，即温泉。世传在昔有蟒为妖，扰害民物，适有大茆真人经其地，以术驱之，害除民安，立寺于地，因有温泉，遂名温泉寺。<br />
　　<br />
　　温泉寺始建于南北朝，刘宋景平元年（公元423年），慈应禅师创建，同时建有缙云寺------等。历经唐、宋、明、清沧桑兴废，古寺于明，倾废又重建，原寺遗址即今卧牛石上至古碑亭一带。宋元年间，高僧志公爱其幽静，曾挂锡于此。景德四年（公元1007年）宋朝廷敕赐名曰“崇胜禅院”。温泉寺从此相当兴盛。历代高僧有慧灌、成聘、常泰、大方、香延、隆树等。本寺为临济正宗传承久远。<br />
　　<br />
　　<br />
　　现有的温泉寺于明宣德元年（公元1426年间）由寺僧真金和尚重建大佛殿、接引殿、塑如来、罗汉等像。真金逝世后，其徒僧祥海续建说法堂、香积堂及前后阶墀、东西两廊、山门、桥道等。明正统八年（公元1443年）僧永灯、永刚又扩建温泉寺所阙至完备。其时温泉寺已具相当规模，成十方丛林寺院，建筑面积比现在大两倍余，僧员百余名，寺年产三千担（租谷）。盛极一时，富甲一方。明末兵乱，寺院又有缺损。清同治二年（公元1869年）又将观音殿换成铁瓦石柱，故又名铁瓦殿。清康熙五十三年又将大佛殿换梁、换柱等工程，修缮一新，至下而上，四殿相接，肃穆壮观。<br />
　　一、关圣殿（原山门殿）：清代改名为关圣殿，塑关羽像。昔传当年关羽败麦城为吕蒙所杀，魂游于湖北玉泉寺上空叫喊：“还我头来”。时为玉泉寺高僧普净禅师点化心悦诚服，稽首归皈作佛弟子，为佛教护法神将，护守于寺中山门殿内。<br />
　　<br />
　　<br />
　　二、接引殿：内供阿弥陀佛，意为无量寿佛，极乐世界教主。生到极乐世界去的人寿命无量，无诸般痛苦。凡是诚心信奉念阿弥陀佛的人，临命终时，阿弥陀佛便来接引到极乐世界去，故又名接引佛。<br />
　　<br />
　　<br />
　　三、大佛殿（又名大雄宝殿）：中供释迦牟尼佛。他创立了佛教，说法四十九年，谈经三百余会，为东土流传万世宝典。<br />
　　<br />
　　<br />
　　四、观音殿（又名铁瓦殿）：中供观音菩萨。观音菩萨有千百亿化身，如千手观音、白衣观音、滴水观音------等多种观音身像。观音菩萨家喻户晓、法力无边、神通广大、心怀慈悲。救苦救难的事迹在民间广流传。于1999年殿内新安座了汉白玉雕塑的观音菩萨、文殊菩萨、普贤菩萨、善才、龙女。左右寮房、楼阁多间，是一座规模高大、雄伟精巧的综合大殿。殿的左右侧两端附属古建筑“花好楼”和“益寿楼”于1989年被公园撤除，殿后右上角有许多凿崖佛像，书刻和一座碑亭，详记各种史实。殿的后面，还有可供几百人活动的平台。四殿现共有建筑面积二千余平方米，各殿中佛菩萨像栩栩如生。<br />
　　<br />
　　<br />
　　四殿呈纵向布置。前靠江、后靠山、前低后高、前小后大，有台阶，通道和小桥把四个大殿连接贯通，放生池环绕于寺内。关圣殿门前的左右两边各建有亭台与之配合，使整个寺院显得非常完整和壮丽。<br />
　　<br />
　　<br />
　　本寺是按建筑年代朝廷所要求的各项标准建成的，是品位极高的宫廷式建筑，充分反映了我国悠久的历史文化艺术和极高的建筑水平。因此，本寺不但是享誉中外的佛教圣地，同时，也是珍贵的省级文物名胜。<br />
　　<br />
　　<br />
　　一九二七年，卢作孚来温泉寺，观其地幽，惊叹曰：乃辟公园之宝地矣，谓寺主隆树和尚曰：“合股辟建公园如何？”隆树正以匪患为忧，即慨然应允，便达成协议，成立起以卢为首的董事会，开始兴建澡堂、旅馆、餐厅等。不断扩建亭台楼阁、浴池，形成如今的温泉公园。<br />
　　<br />
　　<br />
　　北温泉公园的前身即温泉寺，僧众地广。民谚曰：“温三千、禅八百、杉木园的和尚惹不得”。（温指温泉寺，禅即禅岩寺，三千八百指租谷担）当时寺院极富饶，僧精武艺，故有此民谚。自辟公园后，大部份地域为公园取代，公园的筹建，博得许多大军阀的赞助，历史的演变，逐渐形成了园、寺两家悬殊差距。解放后，僧员锐减，又经文革罹难，沧桑多变的古刹范围，也今非昔比。古寺原有两廊及前后间接厅堂附属房廊殿宇，被拆除。如今仅有观音殿两侧一二十间附属房屋可供僧人居住。寺院的厨房被撤除，厕所改作配电房，古碑亭和石刻罗汉像足以说明温泉寺历史渊源变迁。<br />
　　<br />
　　<br />
　　一九三七年抗战开始，战事紧急，国民政府首要机关迁都重庆，达官贵人齐集重庆、北碚避难，一时增加了北温泉公园的知名度。一九四六年才正式命名为北温泉公园。<br />
　　<br />
　　<br />
　　一九四零年，隆树和尚见于当时形势，便将温泉寺契约交付给缙云寺太虚法师作汉藏教理院院产，便由缙云寺统管。<br />
　　<br />
　　<br />
　　解放后，寺庙归国家，人民政府造册登记，立为佛教文物遗产专门档案，不隶属公园新建筑。并非常重视，拨款维修，相继有僧人主持管理。文革浩劫中，破坏了寺院的庄严，僧人被遣散，文物佛像毁失殆尽，管理乱套，国家财富毁于一旦，佛寺已失云昔日风范。文革后期，一九七八年，温泉寺通明法师孤老病苦，迫于离寺，本寺由公园管用。<br />
　　<br />
　　<br />
　　一九八四年经重庆市府（84）129号文批准，重新开放了温泉寺为佛事活动场所，逐步落实了温泉寺佛教房产政策，体现了党和政府对国家宗教政策的重视，并命名为重庆市文物保护单位，如今正着手恢复维修古刹原有的庄严设施，再度展现温泉寺昔日的风彩。<br />
　　名僧太虚大师为了陶铸僧才，振兴佛教，弘法利生，研究汉藏教理，沟通汉藏文化交流，在缙云山创办了世界佛学苑，“汉藏教理院”；著名教育家陶行知先生曾在温泉寺创办“育才学校”；解放后，班禅大师也曾来寺礼佛；朱德、贺龙、杨尚昆、郭沫若、矛盾也先后来温泉寺参观，并留有朱老、郭老的诗词；一九七五年周恩来总理指示国务院拨款修复温泉寺；一九七九年六月六日，温泉寺接待了日本高野山等二十一人组成的佛教代表团参访。如今温泉寺已恢复了大部分古寺风貌，信众如织，香火旺盛，爱国爱教已溶为一体，<br />
