　　写这篇文字的时候我已经做好了被众人口水淹没的准备，我是在唾骂声中长大的，我只想说我想说的，我不想把自己伪装得多么崇高，别人是否伟大跟我无关。<br />
　　<br />
　　宇宙是无限的，地球47亿年相对宇宙连“滴答”一秒都不到就化为尘埃飘向无尽的宇宙空间了；从大陆漂移成功到人类产生，从哥伦布发现新大陆到中华人民共和国的成立，我们一个小小的肉身又算什么？还没有来得及思考就已经由生到死在一瞬间就结束了。我们都只是宇宙的一粒尘埃而已，难道想漂浮的自由一点也不可以吗？还要用自我约定的所谓道德标准作为宇宙物质漂浮的游戏规则？真他吗的太可笑了！<br />
　　<br />
　　“请给老弱病残孕及需要帮助的乘客让个座，谢谢！”<br />
　　我乘公交好多年，每次听到这个缺德屁都是满肚子的气。这口气一直憋在心里，几年了，今天终于有胆量坦白！<br />
　　<br />
　　一、老年人，让中国公交阳痿<br />
　　无须我统计，中国公交给老年人让座的频次是最高的。人们，尤其是年轻人给老年人让座的根源追溯到底无非是在心理上受中华民族“尊老爱幼”的传统“美德”所约束。如果我们年轻人不给老年人让座则被视为不道德、无修养、无公德甚至被人骂作没家教、没良心、没文化等等。<br />
　　而且我注意到了，老年人一上车，中国公交的速度一下子就降了下来，启动慢，行使慢，刹车慢，俨然就是一头被阉割了公牛。这么一来浪费了其他乘客多少宝贵的时间？我常常因此而上班迟到。有很多老年人，上车是一步一步地挪上来的，下车要一步一步的挪下去，看的我他吗的心慌慌的，到底图啥呀！很多人跟在后面等着上下车，要知道现在迟到一分钟可就罚20块钱啊，20块钱要够咱工薪阶层吃几顿的，我要是被罚了，找谁去？<br />
　　中国老年人的体质果真差到在公交上都站不起来的地步吗？我不敢苟同。中国农村的老年人我们不说了，他们的上半辈子被压迫惯了，他们跟老黄牛一起种了一辈子的田，身体素质很好，也用不着坐公交了。生活在城市中的老年人，基本都是反城知青，生活条件比农村好多了，吃了几十年的公粮，我想身体素质就是差也不会差过农民伯伯吧。早上我7：00从家里出发去上班，经过小区花园，看到满满一广场的老年人在锻炼身体，各式健身器材都被他们玩了个遍，而且熟练程度绝对不亚于我这个曾经当过中学体育委员的小青年，他们浑身有使不完的劲！到了夏天的晚上，他们还在一起拉二胡唱京剧，一个个日子过的不亦乐呼。根本就不是需要别人让座的类型！我建议想锻炼干脆到公交上锻炼，那里有高低杠也有吊环，炼呗，怎么一上公交就要坐？<br />
　　再说说我们年轻人，以上班族为例。每天早上一大早就起来在最短的时间内洗涑完毕，当然，有的人还需要大便。有的来不及吃早饭或者根本就不吃早饭就匆匆赶往公交站台了，我个人是每天早上到公司楼下吃早餐的。也就是说，很多年轻人都是空腹乘公交的。说老实话，我每天早上从家到公司要乘一个小时的公交车，乘公交有无座位对我来说非常重要。如果我是从家“坐”到公司的，那么我一整天都会有个好心情，干工作特别带劲，连中午都不需要休息了，一连几天这样的话，老板还会表扬我工作做的好，一高兴就给我加了不少奖金。相反，如果我上了公交没座位，一直“站”到公司，那么我一天都他吗的没劲，甚至会绝望，甚至会怀疑自己的前途，总是不停的问自己为什么每天上班那么辛苦。因为这个原因，所以我每天早上要早起半个小时，为的就是能够等到有空位的公交，不让自己那么辛苦。<br />
　　<br />
　　你说我好不容易弄了个座位，你再让我让给所谓的老年人，我凭什么呀我！上文我已经说的很清楚了，老年人的身体并没有差到非要别人让座不可的地步！话说回来，年纪大了，身体如果的确不行了就别出门了，在家呆着吧您！说我没道德是吧？这话对您那缺德的儿子说去！他怎么能容忍您一把年纪了还去挤公交呢？万一公交来个急刹车，把您摔个粉身碎骨或者半身不遂，这责任该谁来担？是不是这样您的儿子可以拿上一笔保险金或者他压根就没打算让您活过今年？他们把他们应尽的义务不负责任地推给了社会。您一年纪大的出来乘公交给社会带来多大的压力您知道吗？我们年轻人也不容易啊，每天早上不吃早饭乘公交，位置给您让了，您舒服了，我们空着个胃在那晃啊晃的，晃到公司已经不饿了，倒还是省了早饭钱是吧。据不完全统计，中国患胃下垂的年轻人多为乘公交无座位所致。<br />
　　我说句话大家不要不爱听，老年人一命呜呼了对社会产生的影响不大，甚至还减轻了社会的负担，甚至对某些不孝家庭来说是件极大的喜事。我们年轻人就不一样了，如果我死了，那么我的老婆孩子怎么办？我的父母怎么办？祖国会养活他们吗？人民会关心他们吗？不会。如果我死了，我老婆也许会变成一只鸡，我儿子也许会去抢劫，我父母也许会去拣垃圾……我真的不敢想象！<br />
　　我每天工作下来，身体疲惫不堪，肚子都饿扁了。晚上下班，我在公交始发站上车是有座位的，我也不是没有给人让过座，事实上，我让座之后一秒钟内就后悔了，路上超级堵车，一直站了1.5个小时才到家，下车差点就昏过去。我记得我让座的时候，那位老者跟我说了声谢谢，用“谢谢”这两个字换了个座位，真他奶奶的划算，我他吗的还要假惺惺地陪着笑脸说：“不用谢不用谢，您座您座。”今天我可以告诉所有人，我当时内心已经接近崩溃的边缘了。我觉得我很可怜！难道非要用“道德”的外衣来掩饰我虚伪而自私的嘴脸？我不想做个好青年，那是个傻逼！不要跟我提社会公德，我小时候在山区没钱念书没教室上课，咋没人跟我提？不要跟我提人生的价值，人生的价值就是为自己快乐地活着，而且必须快乐，如果不快乐就选择死，没必要赖活着，那是对自己弱小生命的残忍是对他人世界的侵犯！<br />
　　<br />
　　有很多老年人上车之前就已经指望着别人给他让座了，如果他上车之后发现没人给他让座他会浑身不自在并用哀求、疑问甚至痛斥的眼光去打量别人，更有甚者直接就趴在座位扶手上，等于就是硬抢了，在这种情况下，不让座也不行了，年轻人，您就自认倒霉吧。我想他们从今以后一直要麻烦别人去让自己生活地更美好更舒适了！如果是我我会不安的。真正需要公交座位的是我们年轻人，我们年轻人才是身心疲惫的人！我们年轻人才是社会价值的创造着才是推动社会发展的原动力！我们年轻人才是最需要社会关怀的人。多给我们一些爱心吧，社会！不要把所有的压力所以的义务都加在我们的头上！<br />
　　老年人享受的国家津贴太多了，生病了有公费医疗。小时候我们一家几口人生病了，都用爸爸的公费医疗小本本去医院看病，这个办法也是别人教的，没办法，穷啊！现在我在外地没机会享用了，有什么三长两短都要自己掏钱了。我朋友“小卞”大家应该认识吧，不认识的可以去Google搜索《处男今年三十岁》，我们经常在一起说的一句话就是“幸亏自己身体好没得什么大病，所以这几年一直坚持活了下来“！多凄惨啊，我想到这些我就异常激动。我们年轻人有什么津贴？前些年到外地混还要狗屁暂住证，我纳闷在自己祖国的土地上居住怎么叫“暂住”，我要走吧，还要给我加个罪名叫“偷渡”。我朋友小卞是个好人，这样的好男人我想在中国已经绝种了，看过我那帖子的人都知道他，他每次上车都给人让座，他说别人是弱势群体，他每次买菜都比我买的贵，他说现在农民都不容易，如此善良的一个人工资却很低而且经常失业，连女朋友都难找，艰苦的时候他发高烧了就一人躺在床上不打针不吃药不进食直到昏迷，当我发现的时候，我的泪水一下子就涌了出来。我可爱的兄弟，你曾经在QQ上对我发誓不再做个好人，可是你怎么就成不了坏人呢。<br />
　　亲爱的大爷大妈，您的福利待遇那么好，你们其中很多人还因为国家拆迁改造分了几套房子，您就是天天打车天天看戏天天打麻将天天吃香的喝辣的死了再投胎再从新来过一辈子也行啊，您实在没事就买辆宝马奔驰开开，也没人说你啊。您怎么闲着没事非要跑公交上来跟咱小年轻的抢个座位呢？咱要有你那么多钱，我早他吗的买车做老板了。我日。你们真是缺了八辈子的德！<br />
　　<br />
　　同是自然人，我认为老年人跟年轻人有着相对平等的属性。有年过七旬的扒手，这个不希奇；也有头发花白步履蹒跚的嫖客，在南京火车站东侧的“超女洗头房”（雪村出名的时候那里叫翠花美容院）被我迎头撞见；我公司驾驶员还亲眼看到他的爷爷在房间里独自手淫，用他的话说叫“黑里透红、挺拔无比”。贪污犯、卖国贼、恐怖分子有几个年轻的？陈希同、克林顿、李鸿章、拉登这些人有几个年轻的？就说中国省长，有几个年轻的？又有几个能干出多少好事的？举例：江苏润扬（拉索）大桥号称中国第一世界第三，通车典礼的时候江总书记来了，江苏省委书记陈唤友同志对着麦克风说：“下面请江总书记下台剪彩”。就这么一说，时间不长，我们的江书记没下台，我们的陈书记倒是下台了。唉，都是老年人，何必在晚年还要粘上政治这个脏东西。<br />
　　<br />
　　我从来不让自己的父母乘坐公交，我不想让自己的父母像乞丐一样站在那里等待别人极不情愿地施舍！那是对我人格的侮辱。当然现在的中国年轻人好多都是废物，标准的“啃老族”，吃老人的用老人的，自己好逸恶劳，把孩子交给老人每天坐公交接送，既有“老”又有“幼”，不怕没人让坐！这种丑恶现象我早就发现了，太恶毒了，用自己的不道德来博取别人的道德。这跟天桥上假装失学学生骗钱的行经有什么两样？简直就是赤裸裸的道德掠夺！被掠夺的人也是活该的，因为他们虚伪的面纱遮住了他们的视线，看不清狗屁道德的本质，――其实就是一种欺骗！<br />
　　<br />
　　再举一例。一少女与我同乘一公交，有座，一前一后。突然一老妇上车久立于少女身旁，约一分钟左右，少女实在坐不住了，便起身让座。精神矍铄的老妇连说三声谢谢一屁股坐上了位置，少女一脸的踌躇。公交继续往前开，开着开着，少女的屁股就红了。众人窃喜，皆以淫目视之，交头接耳，少女没有察觉。凭我多年的经验，我猜想一定是此女来月经了，一定是用了劣质护垫经不起公交颠簸而出现了“侧漏”现象。我遂起身，以外套裹其臀。少女感激不已，不日即以身相许。<br />
　　我说的这位少女，后来我知道她在外资企业上班。我对她说，你给中国一位绝经的老妇让座却差点红了我们中国女人的脸。<br />
　　<br />
　　我大声疾呼：绝经的老妇，座位还是让给那些流血的少女吧！<br />
　　我奔走相告：阳痿的男人，生命的意义已经基本失去，就别再跟人争了！<br />
　　巴金虽然活了100多岁，但是他说的没错：长寿对他来说是一种惩罚。我想一个人如果失去生活自理能力，那最好的办法就是自杀，不要苟活着，麻烦别人，没意思。男人，60岁以后基本就该OVER了。在我性功能丧失的那天也就是我的末日。<br />
　　我现在每天坐公交都要经过上海市大渡河路，每天在那里都要看到一块牌子，上面用红油漆赫然写着：上海男性性功能恢复中心。我看的很清楚，是“恢复中心”，不是“康复中心”。看得我真是“大渡桥横铁索寒”啊！<br />
　　<br />
　　<br />
　　什么老吾老以及人之老，幼吾幼以及人之幼？！狗屁！我从小学就被欺骗一直被骗到现在，而立之年才大彻大悟真是太可耻了！<br />
　　<br />
　　二、拒绝给儿童让座<br />
　　说起儿童我就想起我们的童年。小时候，上学，每天要走很远的路，有的人还是山路，放学有时候要走过一段坟地，偶尔还遇到疯狗，因为买不起手套，冬天的早上冷得脸上手上都是冻疮，中午了，土路上解冻了，泥泞的道路根本就没法走，母亲亲手做的棉鞋穿坏了一双又一双，从来就没有零食吃，想起来就要流泪……<br />
　　这就是我们70年代人的童年。我们那时候全靠两条腿，没有公交车坐，更别谈谁谁给我们让坐了，实在下雨路滑或遇到什么自然灾害的不是逃学就是母亲背着我们去学校。我记得83年严打，“二王”被击毙了，我上了一年级，有一篇课文说上公共汽车要给老奶奶让座，还配了插图。说实话，那篇课文我一直到1989年大学生绝食都没有读懂，因为1989年之前，我根本就没有见过公共汽车。老师也没有跟我们解释，我估计老师也不知道什么叫公共汽车。<br />
　　<br />
　　再看看现在的孩子，出门上学还有公交车，多幸福啊。还要别人让座？算了吧，小朋友你就站站，有利于骨骼生长，不用买钙片了，不用买搭档了，让什么狗屁的哈药关门吧，让什么狗屎史玉柱回家跟他母亲搭档去吧！没看到现在的孩子一个个的都软不拉几的吗？中国男人越来越没有男人气质了，真他吗的失败！都是中华民族尊老爱幼里的这个“爱幼”给害的，爱什么爱，爱到最后别再像上海的那个谁谁高考完了第2天就自杀，搞到最后都白爱了，还少年作家呢，太脆弱了吧。<br />
　　孩子们，不要坐了，就站着吧。中国人个子已经够矮的了，再坐，就没了。<br />
　　<br />
　　三、拒绝病人上车！<br />
　　扎绷带主拐杖生活不能自理大小便失禁的，我想他们一定在病床上躺着，没机会来乘公交。癌症、性病、爱滋等非传染病或者不借助空气传播的疾病患者在没有明显症状的情况下乘坐公交是无人知晓的，病人不会在自己的脸上刻上“我是病人”4个字，所以没人让座给他们。可怕的是另一种情况：非典、禽流感、肺结核等极具传染性的疾病患者如果一但上了公交，后果不堪设想，而且我听南京医学院一教授说乙肝病毒携带者以及梅毒晚期患者坐过的椅子如果没有及时冷却，是及具传染性的。<br />
　　我觉得在讨论是否给病人让座的问题之前先来讨论一下是不是该允许病人上车这个问题！是不是可以尝试在公交车上安装体温检测仪或者无害X光透视仪，每位乘客上车的时候都用X光照一下五脏六腑看看是不是有疑难杂症传染疾病。有些人在公车上大声咳嗽随地吐痰，看着那难以蒸发的绿色浓痰我阵阵着呕！加上某些老式机车的后置发动机散发出来的毒气，我深感极其需要一个防毒面具。要知道有些公交车是半年才打扫一次的，塑料玻璃窗已经变成半透明的，天窗根本就打不开，底盘上结了厚厚的茧了，有一首歌叫什么来着，叫什么“最爱穿的鞋是妈妈捺的千层底儿啊……”。<br />
　　给病人让座这口号，又是中国公交放的一个虚无缥缈毫无逻辑的屁！<br />
　　<br />
四、残疾人上车逼我成流氓<br />
　　瞎子，前门找不着后门的，他也爬不上来，即使爬上来了，他手里的棍子也不知道会敲到哪位乘客的男根。哑巴，偶尔上来几个，不管是不是装的，多半是一群盗窃团伙，您要是想做好人就给他们让座吧您。还有一类缺胳膊少腿的，脑袋瓜子畸形的都拿着个盆子罐子坐在自己自制的滑板上，从街头滑到巷尾一路乞讨呢，职业的特点决定了丫根本用不着乘坐公交，人家坐着自己的小滑板，想到哪儿到哪儿，不用转车。<br />
　　我9年前被大学开除后就跑去温州打工，结果在公交上被一群江西哑巴扒手围攻，乘务员假装没看见，我挣脱着从行使中的公交车窗口跳了出来，我哭了，那时正我好20岁。后来我就成了流氓。<br />
　　<br />
　　五、孕妇，公交上的怪胎<br />
　　我早说过怀孕跟买房买车一样奢侈。怀不起孕，就别怀！肚子大了就别去挤公交，万一肚子给挤炸了我看你咋办？万一挤出一先天性偏瘫胎儿我看你们夫妻两这辈子怎么过？弃婴当然也不失为一种更好的办法，但是从本质上说你仍然是麻烦他人麻烦社会了。你上车之前难道就真的确认上车一定有人给你让坐吗？凭什么？就凭你撅起的肚子还有那宽大骨盆支撑起来的屁股？还是回去向你老公要打车费吧，他要没钱，你就立刻去医院堕胎吧，不要跟我说什么爱情什么家庭，狗屁。不要让孩子生下来就像你们一样需要靠别人的施舍与帮助才能生活得美好，这样对他人是不公平的。<br />
　　<br />
　　以上观点只代表我张怀旧个人观点，传统道德体系在我的脑海里已经被我彻底粉碎。我活着，不想为道德打工，也无须道德的恩典，只要对得起自己，对得起生我养我的父母和我自己作贱搞出来的孩子，其他的都与我无关。生命的意义到底是什么？生命的意义在于运动！永不停歇的运动！什么叫运动？运动分两种：床上运动和床下运动。做爱是标准的床上运动，无产阶级文化大革命是标准的床下运动。两种运动都有快感，但我觉得后者的快感远大于前者，我更愿意为此类运动付出或生或死的代价。<br />
　　<br />
　　老弱病残孕，请不要再麻烦别人，每个人都有权利让自己生活得更好，如果用道德来压制别人的自由，那其实才是最缺德的！<br />
　　中国公交，老子绝不让座！</div>

							
							
							
							
