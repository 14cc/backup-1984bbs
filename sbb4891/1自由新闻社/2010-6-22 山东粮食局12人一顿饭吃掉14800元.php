<br />
2010年06月22日11:15<br />
<br />
　　近日,山东省粮食局一份餐厅的消费明细账单曝光,人均1000元的消费标准,12个人一顿总共吃掉14810元,被网友称为“天价餐”。山东省粮食局一位工作人员表示“没听说”。山东省纪委值班人员则表示,会立刻上网查询网帖,并交由相关部门处理此事。<br />
<br />
<img src="http://img.hexun.com/2010-06-22/124027984.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
济南市舜耕山庄的“天价”账单 网友图<br />
<br />
　　重庆商报6月22日报道 南京市江宁区房产管理局原局长周久耕因抽150元一包的“九五至尊”被称为“天价烟局长”。近日,一份餐厅的消费明细账单又将矛头指向山东省粮食局――人均1000元的消费标准,12个人一顿总共吃掉14810 元,被网友称为“天价餐”。这份账单在天涯杂谈、网易论坛等全国各大论坛热传,爆料者声称“百分百公款消费”。<br />
<br />
　　每人1000元消费标准<br />
<br />
　　6月20日,网友“tianjiacan”在天涯杂谈发帖《江苏有“天价烟局长”周久耕,山东惊现“天价餐粮食局长”孟庆秀!》,并贴出一张消费明细账单照片。短短一天,20899位网友持续关注,近200位网友回帖讨论。<br />
　　这是张山东省济南市舜耕山庄贵宾楼6688客厅的餐饮消费账单。账单中显示,餐费是1000元/位的标准,12人总共12000元。一壶“日照雪青”260元,一盒软中华118元,一瓶34度白趵精品198元。加上矿泉水、醋饮以及其它零碎消费,这顿饭总共吃掉14810元。这份账单消费时间为 2008年12月30日,而登记的宾客名字则为粮食局。发帖者还表示,就餐的除了山东省粮食局孟局长,还有粮食局两位副局长及3位处长。“百分百公款消费!”<br />
<br />
　　1000元/位算比较高档的<br />
<br />
　　昨日下午,记者拨打该山庄贵宾楼前台电话咨询订餐。服务员明确表示,订餐标准最低150元/人,也可以根据客人需要安排,“1000元/位的标准算比较高档的。”至于1000元/位的标准餐有哪些菜时,服务员称厨师可以依据价位制定菜谱。<br />
　　记者又致电贵宾楼办公室,一位男职员一听到咨询“1000元/位标准订餐”,便警惕地表示:“今天很多记者打听”。接着便以很忙为由挂断电话。随后,该办公室董经理又向记者表示:“贵宾楼没有1000元/位的标准餐”。当记者追问为何与之前员工说法不一致时,董经理坚称“没有这么高”。对于网上流传的那份账单明细,董经理表示“不太清楚”便匆匆挂掉电话。<br />
<br />
　　山东省纪委:立即调查<br />
<br />
　　昨日下午5点左右,记者联系上山东省粮食局,办公室一位赵姓工作人员表示“没听说”,同时拒绝提供领导电话。直到记者截稿前,粮食局多个科室均无人接听电话。山东省纪委值班人员则表示,会立刻上网查询网帖,并交由相关部门处理此事。对于相关人员的具体处理结果,本报将继续予以关注。<br />
<br />
　　对话：这种档次的消费不多见　<br />
<br />
　　昨日,记者几经周折联系上了济南市舜耕山庄的一位内部工作人员吴心（化名）。<br />
　　记者:这个账单是不是真的？<br />
　　吴心:应该是真的,这是山庄的账单。贵宾楼是山庄举办大型活动、重要活动的地方。<br />
　　记者:山庄在济南算不算档次很高的？<br />
　　吴心:中上吧,属于四星级。<br />
　　记者:1000元一人的消费是不是最高的？<br />
