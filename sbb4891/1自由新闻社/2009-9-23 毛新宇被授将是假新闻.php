2009-09-27 14:34:37　来源: 金羊网-羊城晚报(广州)　<br />
<a href="http://news.163.com/09/0927/14/5K7NMTNG0001124J.html" target="_blank">http://news.163.com/09/0927/14/5K7NMTNG0001124J.html</a><br />
