<br />
于是，问题来了，人们应该相信毛新宇的哪一个秘书?或者说，人们应该相信毛新宇过去所说的，还是他的秘书现在所说的?<br />
<br />
毛新宇是中国军事科学院战争理论和战略研究部副部长，副军职研究员，全国政协委员。<br />
