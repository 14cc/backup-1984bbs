<br />
记者： 丁力 <br />
华盛顿<br />
Sep 24, 2009<br />
&nbsp;&nbsp;<br />
<br />
近期中国的一个热门新闻是，毛泽东唯一的孙子、39岁的毛新宇升为少将了。但是最近香港文汇报援引毛新宇秘书的话说，那是媒体误报。<br />
<br />
我们早就注意到关于毛新宇升少将的说法，但是鉴于消息和照片之间的矛盾，不同来源的消息之间的矛盾，我们无法确认，所以没有跟风报道。<br />
<br />
最新消息是，香港文汇报说， 毛新宇的秘书郭景良9月23日证实，毛新宇目前军衔仍为大校。郭景良表示，他陪同毛新宇参加过一些活动，很多记者采访时称毛新宇为少将，毛新宇和他每次都纠正。他希望大家不要听信、炒作有关军衔的传言。<br />
<br />
传言从何而起?让我们追本溯源。<br />
<br />
*湘风起兮云飞扬*<br />
<br />
传播毛新宇升少将之说最多的是“毛主席的故乡人”--湖南人。<br />
<br />
湖南日报6月21日报道， 6月19日，湖南省政府驻北京办事处日前邀请毛新宇少将为省驻京单位工作人员讲党课，迎接建党88周年。<br />
<br />
后来，湖南省各级政府网站和媒体多次提到毛新宇少将。<br />
<br />
*毛新宇自称将军?*<br />
<br />
广东的《南方人物周刊》在9月刊登对毛新宇的专访，引用他的话说：<br />
<br />
“我没有想到我会参军入伍，更没有想到自己会当将军。”<br />
<br />
“现在我觉得这两件事不容易，一个是当好这个将军、学好毛泽东思想不容易；另一个是教育子女也不容易。”<br />
<br />
香港大公报9月21日报道。新闻资料显示，毛新宇最近出席湖南省国际旅游节开幕式、为某驻京单位讲党课时所用的身份是少将。“39岁的毛新宇成为解放军最年轻的一位将军，并且是第一位也是唯一的「70后」将军。”<br />
<br />
*秘书通知称少将*<br />
<br />
