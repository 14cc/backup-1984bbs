羊城晚报9月27日报道 毛泽东嫡孙、毛岸青与邵华之子、全国政协委员、中国人民解放军军事科学院战争理论和战略研究部副部长毛新宇继承母志，前日正式出任广州松田职业学院名誉院长，趁着直面毛新宇本人的机会，记者从新近“炒”得正热的“将军论”切入，细听他的剖白。<br />
<br />
少将军衔<br />
<br />
最快明年“八一”前被授予<br />
<br />
羊城晚报：最近大家都陷入了疑惑，应该称您为毛将军还是毛大校？<br />
<br />
毛新宇：叫我毛部长吧，我还没升将军呢。其实啊，大家都误会了。在此，我有必要解释一下。早前，中央文献在长沙成立毛泽东生平与思想研究峰会，我受邀参加了活动，会后还顺道去了湖南国际旅游节现场。关于旅游节的活动，新闻通稿是由长沙旅游局发出的。在最早的通稿里面，我其中一项的身份介绍是中国人民解放军军事科学院战争理论和战略研究部副部长，军衔为大校。但长沙旅游局的领导后来觉得这个称呼不对，按地方的理解，副部长应该就是副军，那就是将军！于是，他们赶紧把我的军衔改成了少将。从此，消息被传开了。<br />
<br />
羊城晚报：关于您军衔的事情，还有个疑问。您近期曾接受过某人物周刊的采访。当时，您曾提到过自己“有三个想不到”。其中的一个“想不到”：您提出，没想到自己会当将军。那么，这个军衔怎么又成了将军了呢？<br />
<br />
毛新宇：是这样的。去年，我母亲邵华去世，Hu Jintao主席下了道命令，于2008年7月份任命我为中国人民解放军军事科学院战争理论和战略研究部副部长，副军级。我2000年入伍读博士，读完博士留在军科院工作，至今军龄已九年多，快10年了。按军队规定，军龄满10年的，可以竞争晋升少将。至于我现在是否为少将，我回答：未来是，现在不是。如无意外，最快我可能在明年“八一”前被授予少将军衔吧。<br />
<br />
羊城晚报：按常规，明年您如果晋升为将军，将是国内最年轻的将军吧。<br />
<br />
毛新宇：对。在我这个年龄，有那么高的荣誉和地位，我觉得这是全国人民对领袖家族的尊敬和爱戴，也是党中央对开国元勋后代的培养和关心。当然，这里面也有我自己努力的一部分，未来我还将继续努力。<br />
<br />
国庆阅兵<br />
<br />
受邀天安门观礼台上观看<br />
<br />
羊城晚报：曾有媒体猜测，今年的国庆60周年阅兵式，您将会在观礼台上，是吗？<br />
<br />
毛新宇：是的。受党中央领导的关怀，我今年受邀安排到天安门的观礼台上观看大型阅兵仪式。<br />
<br />
羊城晚报：今年国庆60周年，对您而言，会有另外一种意义吗？<br />
<br />
毛新宇：确有另外一层深刻含义。新中国成立60周年，对于每位中国人来说，这都是一个值得纪念的日子。为建立新中国，毛家共牺牲了十几位烈士，国庆的意义于我们就显得更为特别。前不久中央举行的“双百人物” 评选活动，奶奶杨开慧，叔公毛泽民、毛泽覃当选为新中国成立作出突出贡献的英雄模范人物“百位人物”；伯父毛岸英当选新中国成立以来感动中国的“百位人物 ”。日前，中央领导在纪念堂接见了我们这些“双百人物”的后代。随后，我特地写了篇文章，主要是纪念奶奶杨开慧。<br />
<br />
羊城晚报：能不能透露一下，您在纪念奶奶的这篇文章里，写的都有些什么内容和细节呢？<br />
<br />
毛新宇：好。文章中我引用了爷爷怀念她的诗词《蝶恋花・答李淑一》，然后我回顾了奶奶生平的一些主要事迹，还有她牺牲的前后经过。其实，我出生的时候，奶奶已经过世一段时间了。她是1930年牺牲的，离现在时间不短了。我对她的了解，多来源于我的父母。值得提出的是，明年是杨开慧烈士牺牲80周年。其实，我写这个文章为的是纪念我的奶奶，为的是纪念牺牲的烈士。<br />
<br />
个人素质<br />
<br />
比应试教育的成绩要重要<br />
<br />
羊城晚报：最近听说您在广州松田职业学院当上名誉院长，您是继承母亲邵华将军的教育事业而来的吗？<br />
<br />
毛新宇：是。母亲生前尽管身为中国摄影家协会主席，但她本人一直对教育事业十分关注，尤其是民办教育事业。2007年9月，应增城市松田实业有限公司的邀请，母亲正式出任广州松田职业学院院长。那年，母亲曾说过：“毛新宇是我的后备军，我不能到松田上课了，就派新宇来。”母亲逝世，为此，我出任学院的名誉院长，未来还会给学生讲课。在今年学院2009级新生开学典礼暨军训总结大会上，我引用了爷爷毛主席说过的话激励新生：“你们青年人正是朝气蓬勃，像早晨八九点钟的太阳。”<br />
<br />
羊城晚报：我们留意到，您一直都有关心我国素质教育和应试教育这方面的事情。<br />
<br />
毛新宇：现在我们国家正在从应试教育到素质教育的转型期，我个人认为，这个转型期的时间可能还比较长一点，这需要全社会的思想观念的接受和认可。其实我觉得，相对于一个受教育成长的人来说，个人素质能力可能比应试教育的成绩要重要一些。<br />
<br />
毛泽东思想系<br />
<br />
希望广州高校能率先开设<br />
<br />
羊城晚报：您是专注研究毛泽东思想的，对于当代大学生学习毛泽东思想，您有什么样的建议？<br />
<br />
毛新宇：我跟松田职业学院一直在商量，我也一直希望能在广州的高校里面率先开设毛泽东思想系，从而对青少年进行毛泽东思想主题教育。这是我跟学校共同努力的方向，正在朝这个方向努力。目前，国家教育部对毛泽东思想教育，对青年人、大学生的教育也是很重视的。<br />
<br />
羊城晚报：9月9日是毛主席的逝世纪念日。但9月9日通常又是新人喜结连理的日子。您如何体验这两种截然不同的氛围？<br />
<br />
毛新宇：对于领袖的生日和祭日，我认为国家应该作个特别的声明。我想，国家既然能提案对端午、清明、中秋等传统节日以放假的形式纪念，那么也能有相关形式来纪念开国领袖的生日、祭日以及我国革命史上一些重大事件日期，这对加深人们对中国历史传统的认同会有积极意义。明年的“两会”上，我会把这些想法作为提案内容，写出来、交上去。<br />
<br />
人物档案<br />
<br />
毛新宇，新中国缔造者毛泽东的嫡孙、毛岸青与邵华之子。1970年1月17日生，中共党员，中国人民解放军军事科学院战争理论和战略研究部副部长、中华全国青年联合会常委、全国政协委员。<br />
<br />
1992年7月毕业于中国人民大学历史系；1992年9月至1995年7月在中央党校理论部攻读硕士学位；2000年进入军事科学院攻读博士学位，同年参军入伍，2003年7月获博士学位；曾任军事科学院战争理论和战略研究部正师职研究员，大校军衔。2008年7月，被任命为军事科学院战争理论和战略研究部副部长，副军级，大校军衔。<br />
<br />
从1990年起发表文章和出版著作，主要作品：文章《会当击水三千里》；图书《朱元璋研究》、《爷爷毛泽东》等，主持过电视专题片《女红军女将军风采录》、《江山如此多娇》。<br />
<br />
毛新宇妙言<br />
<br />
●新中国成立60周年，对于每位中国人来说，这都是一个值得纪念的日子。为建立新中国，毛家共牺牲了十几位烈士，国庆的意义于我们就显得更为特别。<br />
<br />
●至于我现在是否为少将，我回答：未来是，现在不是。如无意外，最快我可能在明年“八一”前被授予少将军衔吧。<br />
<br />
●其实我觉得，相对于一个受教育成长的人来说，个人素质能力可能比应试教育的成绩要重要一些。<br />
<br />
