													<h2>圣元奶粉被疑致女婴性早熟 多地出现同类病例</h2>
						
						
																											<div id="postmessage_774881" class="t_msgfont">[导读]截至8月5日，江西省奉新县10个月女婴、山东省临沂市8个月女婴出现早熟症状，另有广东湛江3个月男婴雌激素检测超标，他们均自出生就喝圣元奶粉。另外，家长申请奶粉检测遭到质检机构拒绝。<br />
<br />
<br />
4个月大的小彤在妈妈怀里哭闹，桌上摆满了她吃过的奶粉包装盒（已做技术处理）<br />
<br />
<br />
<br />
尽管已经停用该奶粉，小菲看到奶粉罐还是很兴奋。刘永晓/摄<br />
<br />
圣元奶粉疑致女婴性早熟 广东江西山东现同类病例<br />
<br />
截至8月5日，记者接到读者反映：江西省奉新县10个月女婴、山东省临沂市8个月女婴出现早熟症状，另有广东湛江3个月男婴雌激素检测超标，他们均自出生就喝所涉品牌奶粉。<br />
<br />
据圣元官方网站介绍，圣元营养食品有限公司已“闯进中国婴幼儿奶粉市场份额前三强”。该介绍称，1998年，集产品研发、生产、销售、咨询服务为一体的专业婴幼儿乳品企业青岛圣元乳业有限公司在美丽的奥运之城青岛诞生。平均每年70％的惊人增长速度，让圣元这个年轻的品牌飞速闯进中国婴幼儿奶粉市场份额前三强(中华商业信息中心CIC2008年5月份数据显示，圣元在中国婴幼儿奶粉市场份额达到10.66％，位居第二位)，营销网络遍布全国30个省市的2336个区县。2005年，公司在美国纳斯达克成功上市，成为国内营养食品企业正式在美国上市的第一家。2007年更名为圣元营养食品有限公司。<br />
<br />
三天前，“武汉三名女婴性早熟”的病例经《健康时报》报道后引发社会强烈关注。记者了解到，武汉三名女婴家长称自己的孩子食用圣元奶粉，因此她们怀疑是圣元奶粉惹的祸。<br />
<br />
与此同时，在广东、山东、江西都发现同类病例。<br />
<br />
武汉市儿童医院姚辉教授向记者介绍说:“从上个星期开始登记以来，我经手的有四例女婴性早熟病例，一年估计有几十例。”据了解，湖北省卫生厅等相关部门已着手了解女婴性早熟的病例情况。<br />
<br />
目前，引发女婴性早熟的原因仍无定论，相关机构出言谨慎。不过，圣元营养食品有限公司昨日向记者表示，“我们的产品没有问题”。<br />
<br />
“圣元优博，58种营养素，让妈妈的爱没有缺憾。”这句电视广告词，令无数中国年轻妈妈倍感温馨。然而现在，圣元奶粉却因“女婴性早熟”事件饱受质疑。<br />
<br />
几天前，“武汉三名女婴性早熟”的病例经《健康时报》报道后引发社会强烈关注。记者了解到，武汉三名女婴家长称自己的孩子食用圣元奶粉，因此她们怀疑是圣元奶粉惹的祸。<br />
<br />
武汉市儿童医院姚辉教授说：“从上个星期开始登记以来，我经手的有四例女婴性早熟病例，一年估计有几十例。”据了解，湖北省卫生厅等相关部门已着手了解女婴性早熟的病例情况。<br />
<br />
目前，引发女婴性早熟的原因仍无定论，相关机构出言谨慎。<br />
<br />
问题女婴一直用的是圣元奶粉<br />
<br />
在“武汉三名女婴性早熟”病例曝光后，记者昨日联系上其中一位涉事母亲――湖北省武汉市的邓小云。<br />
<br />
据邓小云介绍，今年6月份，她发现才一岁出头的女儿出现了性早熟症状，主要表现为两个乳房提前发育，皮肤下面有明显的硬核，同时阴道还出现了发炎的症状。“开始以为没什么大的问题，没有想到约一个月之后，这些症状还没有消除。”无奈之下，邓小云于7月5日带着女儿到武汉市儿童医院就诊。武汉市儿童医院的老教授江泽熙初步断定是激素引起的性早熟。<br />
<br />
“江医生当时说孩子太小，不用做检查，但我还是不放心，于是又带着女儿到湖北省妇幼保健院进行了相关的检查，证明确实是激素惹的祸。”武汉市儿童医院市场宣传部的张姓主任证实，包括邓小云女儿在内，该医院近来发现了多名女婴性早熟的病例。他还介绍说，“儿童性早熟的病例较为常见，因为很多都是门诊，所以具体有多少例无法统计。”<br />
<br />
另据武汉市儿童医院内分泌科、院中心实验室副主任姚辉教授介绍，近几年来，儿童性早熟的病例慢慢地增多，但两岁以下的女婴性早熟并不常见。“从上个星期到现在，有四个女婴性早熟的病例，以前的没有统计过，一年下来估计有几十个。”<br />
<br />
邓小云告诉早报记者，她带着女儿去武汉市多家医院就诊，医生给出的建议是停止食用奶粉或者换个品牌的奶粉食用。“停用奶粉之后，女儿阴道炎的症状有了明显的缓解，但是乳房仍有硬核。”<br />
<br />
邓小云由此怀疑，可能是孩子食用的青岛圣元优博奶粉出了问题。“我们是自然怀孕，没有用过任何的避孕药物。女儿出生后的一年多，我们一直用的是圣元奶粉，孩子就是吃奶粉，我觉得奶粉可能就是罪魁祸首。”<br />
<br />
圣元被指曾想赔20万<br />
<br />
据《健康时报》报道，巧合的是，武汉市另外两名性早熟的女婴，所食用的为同一品牌奶粉。据邓小云称，在治疗期间，圣元的代理商曾数次找她了解孩子病情，并提出了赔偿意愿。“最开始圣元说愿意出2000元，我没有同意。后来有一次无意中说到20万元赔偿，如果我当时同意，他们应该会愿意协商。”<br />
<br />
邓小云说，之所以没有接受圣元赔偿的提议，是想先治好孩子的病和了解到底是什么原因致病的。“如果他们的奶粉没有问题，他们为什么要出钱？”<br />
<br />
虽然医院的医生也怀疑女婴性早熟可能与食用的奶粉有关，但他们仍然出言谨慎。<br />
<br />
据姚辉介绍，以前没有想到把相关的病例作登记和总结，从近期她经手的几个病例来看，在病情上有一些共同性，“但到现在为止，还没有看到与奶粉之间是否有必然的联系，但也不能断定奶粉就一定没有问题。”<br />
<br />
对于到底是哪个环节出现问题，目前各方仍无定论。向有“乳品业大炮”之称的原中国奶协常务理事王丁棉，建议记者对原奶生产环节进行调查。他认为，奶粉中含激素的情况以往很少见。“如果有问题可能也是出现在奶牛产奶的这个环节，因为不排除有养殖者使用激素以提高奶牛的产量。”<br />
<br />
<br />
圣元称“不可能让婴儿性早熟”<br />
<br />
针对“武汉三女婴早熟”的相关报道，圣元奶粉公司公关部负责人张迎玖此前在接受凤凰网采访时表示，已从网上得知消费者将奶粉送检，但官方检测机构至今未公布结果。<br />
<br />
对于圣元奶粉是否会引发婴孩性早熟，张迎玖坚称，婴孩性早熟的成因是多方面的，但不可能是因为食用奶粉引起的。“我可以肯定地告诉你，我们的产品不可能让婴儿性早熟。”<br />
<br />
而对于《健康时报》报道中提及的三名婴儿，是否就是经销商所反映的出现性早熟的婴儿，圣元方面称，由于报道中用的是化名，他们不知道是不是“经销商反映的消费者”，“不过，武汉经销商只反映说有一个出现这种情况。”他强调，圣元每个批号的产品都是经过检测上市的。“三聚氰胺事件后，国家的要求逐步提高，从原料到出厂成品都要检测才能上市。我们的产品没有什么问题。”<br />
<br />
质检部门不受理个人申请<br />
<br />
弄清病理最直接的方法是对奶粉进行检测，然而相关的检测机构不接受个人的申请，而权威部门关于奶粉的检测指标中也没有激素这一项。<br />
<br />
此前，邓小云等家长也曾想把奶粉送检，却遭遇“检测无门”，一些质量监督检测机构都婉言拒绝了家长们的个人申请。<br />
<br />
记者致电获国家质检总局授权的两大国家级乳制品质量检验中心――位于哈尔滨的国家乳制品质量监督检验中心和位于呼和浩特的国家乳制品及肉类产品质量监督检验中心。这两个机构工作人员均表示，无法对奶粉进行激素检测。两家机构均表示，“我们只能检查奶粉国家标准里面规定的项目，激素并不在其中。”<br />
<br />
上述两处检验中心工作人员告诉记者，实际上，激素属于药物，奶粉里面是不能含有激素的，“一点含量都不能有”。他们同时表示，如果想检测，建议去药检部门或卫生部门试试。<br />
<br />
国家药监局负责药品检测的中国药品检验总所负责检品受理业务的工作人员称，除某些保健食品之外，目前我国药检部门尚未开展食品检测业务，“要检测奶粉中的激素成分，现在肯定不能送检，而且不受理个人申请”。<br />
<br />
武汉市儿童医院姚辉教授表示，由于性激素有十多个分类，而医院的设备有限，目前仍无法对奶粉中是否含有性激素进行检测。作为主管商品流通的行政单位，工商局可以抽检奶粉，但在目前婴幼儿奶粉的检测标准中，并没有“激素检测”这一项。据东方早报<br />
<br />
一周出现4个性早熟病例<br />
<br />
在“武汉三名女婴性早熟”病例曝光后，记者联系上其中一位涉事母亲――湖北省武汉市的邓小云。<br />
