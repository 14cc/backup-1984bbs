另据武汉市儿童医院内分泌科、院中心实验室副主任姚辉教授介绍，近几年来，儿童性早熟的病例慢慢地增多，但两岁以下的女婴性早熟并不常见。“从上个星期到现在，有四个女婴性早熟的病例，以前的没有统计过，一年下来估计有几十个。”<br />
<br />
邓小云告诉记者，她带着女儿去武汉市多家医院就诊，医生给出的建议是停止食用奶粉或者换个品牌的奶粉食用。“停用奶粉之后，女儿阴道炎的症状有了明显的缓解，但是乳房仍有硬核。”邓小云由此怀疑，可能是孩子食用的青岛圣元优博奶粉出了问题。<br />
<br />
圣元被指曾想赔20万<br />
<br />
据《健康时报》报道，巧合的是，武汉市另外两名性早熟的女婴，所食用的为同一品牌奶粉。据邓小云称，在治疗期间，圣元的代理商曾数次找她了解孩子病情，并提出了赔偿意愿。“最开始圣元说愿意出2000元，我没有同意。后来有一次无意中说到20万元赔偿，如果我当时同意，他们应该会愿意协商。”<br />
<br />
邓小云说，之所以没有接受圣元赔偿的提议，是想先治好孩子的病和了解到底是什么原因致病的。“如果他们的奶粉没有问题，他们为什么要出钱？”<br />
<br />
虽然医院的医生也怀疑女婴性早熟可能与食用的奶粉有关，但他们仍然出言谨慎。<br />
<br />
据姚辉介绍，以前没有想到把相关的病例作登记和总结，从近期她经手的几个病例来看，在病情上有一些共同性，“但到现在为止，还没有看到与奶粉之间是否有必然的联系，但也不能断定奶粉就一定没有问题。”<br />
<br />
对于到底是哪个环节出现问题，目前各方仍无定论。<br />
<br />
专家、职能部门出言谨慎<br />
<br />
与三名女婴的家长高度怀疑是奶粉引起婴儿性早熟的判断相比，相关医院和职能部门显得更为谨慎。“目前没有足够的证据证实，婴儿的性早熟到底是不是由奶粉引起的。”武汉市儿童医院市场宣传部的张姓主任表示，在7月底时，湖北省卫生厅、质监局、食品卫生监督局和工商部等部门开了个会，对婴儿性早熟问题进行了研讨，“会上专家也说，儿童性早熟确实不少见，但婴儿性早熟的却比较少见。在会上也没有得出结论，但要求我们把情况及时上报。”<br />
<br />
记者随后致电湖北省卫生厅，该厅医政医管处的朱姓处长介绍，“卫生厅当时咨询了专家，他们认为性早熟的情况比较正常，原因也比较复杂。”他劝记者说，“我建议你不要插手这个事，婴儿性早熟的原因是复杂的。”对于记者关于婴儿性早熟的病例是否与食用奶粉有关的提问，朱处长不愿多谈，他让记者找政策法规处或质监局了解情况。<br />
<br />
记者随后致电湖北省质监局，该局办公室人员称目前还不知道质监局是否介入了调查，“如果产品在生产过程中出现问题，就由我们负责，如果是销售中出现问题应该由工商局负责。”<br />
<br />
圣元公关部<br />
<br />
我们的产品不可能让婴儿性早熟<br />
<br />
针对“武汉三女婴早熟”的相关报道，记者致电圣元奶粉公司公关部。<br />
<br />
该部门负责人张迎玖的助理王先生告诉记者，根据他们了解到的消息，湖北武汉有关部门已经介入此事的调查。这位工作人员向记者介绍:“我们和消费者想的一样，也希望检测部门把结果尽快公布出来，这是现在解决这种猜疑最有效的方法。这样，就不会引起消费者的猜疑，包括我们企业也在受到一些损失。”<br />
<br />
此前，张迎玖在接受采访时表示，已从网上得知消费者将奶粉送检，但官方检测机构至今未公布结果。“7月中旬曾有消费者前来交涉，公司方面表示让消费者送检，如果产品确实有问题，公司可以提供赔偿。但消费者并未接受。”<br />
<br />
对于圣元奶粉是否会引发婴孩性早熟，张迎玖坚称，婴孩性早熟的成因是多方面的，但不可能是因为食用奶粉引起的。“我可以肯定地告诉你，我们的产品不可能让婴儿性早熟。”<br />
<br />
质检部门 <br />
<br />
不受理个人申请且无激素检测<br />
<br />
弄清病理最直接的方法是对奶粉进行检测，然而记者在采访中了解到，相关的检测机构不接受个人的申请，而权威部门关于奶粉的检测指标中也没有激素这一项。<br />
<br />
此前，邓小云等家长也曾想把奶粉送检，却遭遇“检测无门”，一些质量监督检测机构都婉言拒绝了家长们的个人申请。<br />
<br />
记者分别致电获国家质检总局授权的两大国家级乳制品质量检验中心――位于哈尔滨的国家乳制品质量监督检验中心和位于呼和浩特的国家乳制品及肉类产品质量监督检验中心。这两个机构工作人员均表示，无法对奶粉进行激素检测。两家机构均表示，“我们只能检查奶粉国家标准里面规定的项目，激素并不在其中。”<br />
<br />
上述两处检验中心工作人员告诉记者，实际上，激素属于药物，奶粉里面是不能含有激素的，“一点含量都不能有”。他们同时表示，如果想检测，建议去药检部门或卫生部门试试。<br />
<br />
记者随后致电国家药监局负责药品检测的中国药品检验总所。该所负责检品受理业务的工作人员称，除某些保健食品之外，目前我国药检部门尚未开展食品检测业务，“要检测奶粉中的激素成分，现在肯定不能送检，而且不受理个人申请”。<br />
<br />
此外，有消息人士透露，武汉市疾控中心日前在收到3名女婴疑因食用奶粉出现性早熟的相关材料后，已将问题上报至武汉食品安全委员会，但武汉市疾控中心称目前无法就此事发表意见</div>

							
							
							
							
