<br />
博讯记者从习近平办公室得到的消息称，新年伊始，习近平已经进入战斗状态。习办的一位李姓秘书说，习近平务实能干，没有权力欲，能够走到今天完全是实干出来的，加上他为人谦和，尊重老一辈，深得上下信任。他说，习近平成为十八大的最有希望的接班人也并不是像外界所言是江泽民隔代指定。邓小平早在指定胡锦涛的时候，就恢复到自己在上个世纪八十年代初的想法，要求一定要培养自己的后代。八十年代初，邓小平最早树立的接班人是现任上海市委书记俞正声。但由于俞正声的哥哥俞长声被中情局策反叛逃而失去了对太子党的信心。1989年六四后，他又改变主意，希望多培养高干子弟，这样才是永保江山的良策。<br />
<br />
这位告诫不要透露姓名的秘书说，本来习近平接班已经铁板钉钉，可偏偏有人要用什么党内民主阻碍习近平接班，这使得本来对接班没有兴趣，一切愿意服从党组织安排的习近平很不爽，习办的人更是群情激愤。他又提到重庆的薄熙来用哗众取宠的方式博上位，不但 破坏了法治，而且也把中共当今的各位领导都得罪了。不过，薄熙来可能会走极端，到时要用民意推举他上位。这位秘书嘲笑说，在有薄熙来参加的各个选举中，他几乎都是敬陪末座的。所以，他这次要冒险打黑来博民意。 (博讯 boxun.com)<br />
<br />
秘书说，他当然也知道还有三个地方的大员在搏上位，包括以思想解放为口号的汪洋书记。他说，他个人对汪洋没有成见，但他要想越过习近平接班，实在是太不实际了。虽然他说，胡锦涛最欣赏的地方大员就是汪洋。(boxun.com)<br />
<br />
习办秘书说，看到这些人在各地大肆表演，弄政绩，搏民意，习近平一直保持低调，但2010年到来后，习办也准备“投入战斗”，他说，虽然都是习办智囊的主意，但习近平不但没有反对，还积极出谋划策，鼓励大家。<br />
<br />
秘书说，2010新年一开始，全国基层党建工作手机信息系统开通，习近平就以个人名义同时向100万名基层党支部书记发送短信问候，这件事是前所未有的，收到信息的党员领导干部一下子和习近平拉近了距离。(boxun.com)<br />
<br />
习办正在研究，是否进一步开通信息回复和沟通功能，如果一旦开通，那在共和国历史上属于第一次。这位秘书说，习近平有很多资源，这个直接和全国各地党支部书记沟通的方式，就是其他几位想上位的“黑马”所没有的。<br />
<br />
秘书向博讯记者透露，2010年是一个不平凡的开端，别以为这只是一条简单的手机信息，习办正是要通过这个信息让一些人知道，你可以博民意，也可以弄改革，但和基层党组织更近的是习近平。也向那些不自量力的竞争者宣布，各地基层党组织将会是习近平的地盘，他今后将要以此为基础，同那几位想借助“党内民主”上位的黑马们玩一下。</div>

							
							
							
							
