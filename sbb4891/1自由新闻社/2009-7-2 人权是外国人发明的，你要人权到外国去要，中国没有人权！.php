我疑惑了一下，在电脑上核实身份不是瞬间完成的事吗?<br />
<br />
两位山东老乡说，肯定去和我们那勾结去了。<br />
<br />
我笑了一下，心里受孙东东影响，还是觉得这些访民都有点言过其实。<br />
<br />
又等了十几分钟，过来说，你过来一下。<br />
<br />
李淑莲出去了。<br />
<br />
又过了几分钟，李淑莲说让你们都过去。<br />
<br />
我知道，那警察是不愿意我参与的，但是没有我经常在中间做翻译，他们语言沟通有困难，所以才让我去的。<br />
<br />
到了另一个办公室，每个办公桌隔成小格子的。<br />
<br />
看样子是正式开始做笔录了。<br />
<br />
警察开始敲键盘。<br />
<br />
每一句话都十分艰难。<br />
<br />
省略姓名籍贯的问话。……<br />
<br />
警察：你说你丢钱了，谁能证明？<br />
<br />
娥大姐：我们都能证明。<br />
<br />
警：你看见了吗？你看见谁从她的兜里往外掏出4700元钱来了吗？<br />
<br />
娥大姐：那我没看见。<br />
<br />
警：没看见你说什么？<br />
<br />
你说你有4700元钱，谁能证明。<br />
<br />
李淑莲：我确实有4700块钱啊，我还能虚说吗？<br />
<br />
警：你说不行啊，我还说我有十万块钱呢。<br />
<br />
我说：那您说怎么办呢？她确实钱不见了。犯罪分子取人钱财的时候肯定不会让人看见的啊。<br />
<br />
警：现在是重证据。你没人看见你有钱，这事就定不下来。<br />
<br />
我对李淑莲说，有人看见你有钱了吗？<br />
<br />
娥大姐说，我看见了。我看见她擦澡的时候，这里鼓鼓囊囊的，我就问她怎么那么鼓鼓囊囊的，她说把钱藏在这了。<br />
<br />
警：你看见她数钱了吗?<br />
<br />
没有。<br />
<br />
那就证明不了什么。还可能把报纸鼓鼓囊囊的装进去呢。<br />
<br />
我说，一般来讲不会有人把报纸叠起来仔仔细细缝在裤腰上的。<br />
<br />
那可说不定。你都是推理。现在是重证据。<br />
<br />
……<br />
<br />
在这个问题上来回扯了二十几分钟。<br />
<br />
手机怎么回事？<br />
<br />
被他们抢走了。<br />
<br />
是不是从你手里抢走的？是掰开你的手抢走的吗？说不定是掉在地上了，你屋里那么多的人。<br />
<br />
就是从我手里抢走的。后来我跟他们要的时候，他们说不给。<br />
<br />
出去接电话，半个小时。<br />
<br />
这时，两位山东老乡又说，他这是在故意拖延时间。<br />
<br />
我说，没关系让他拖延吧，反正这案得报上。<br />
<br />
……<br />
<br />
警察回来，继续。<br />
<br />
还有什么？<br />
<br />
他们还把我赤身裸体拖出去，一丝不挂啊。<br />
<br />
谁能证明？<br />
<br />
我，娥大姐说。<br />
<br />
我说，刚才给你的那个名单都是跟她住一个屋的，都愿意作证。<br />
<br />
看名单。说，那也不行。签了名也不一定愿意作证。<br />
<br />
就是因为愿意作证才签的名。<br />
<br />
现场作证得两个人才行。<br />
<br />
你要再叫一个人吗？还可以马上叫一个来。<br />
<br />
你到北京干什么来了？<br />
<br />
我上访啊。<br />
<br />
那他们接访的是工作我管不了。<br />
<br />
截访就能把我那么样侮辱啊？<br />
<br />
我说，截访的过程也不能违反宪法啊？<br />
<br />
人家是工作行为。<br />
<br />
我：十几个大汉赤膊穿着大裤衩，把人从床上拖起来，一丝不挂拖出室外，而且20多米，这也是工作行为吗？这不是侮辱吗？<br />
<br />
警：一个人你说是侮辱，两个人以上就是工作行为。<br />
<br />
两个人以上就可以为所欲为了吗？<br />
<br />
人家截访的都是有手续的，那咱也管不了。不是我们管的事。<br />
<br />
截访的，有手续就可以违反宪法了吗？<br />
<br />
那个我不管，我只管实际损失，比如丢钱了，丢手机了。<br />
<br />
李淑莲：那我人格被侮辱呢。<br />
<br />
我怎么跟你说不明白呢？那不归派出所管。派出所不能什么都管。是不是，公检法各负其责。你找法院去投诉。<br />
<br />
那我人权被侵犯了，也不管了？<br />
<br />
<strong>你别跟我提人权，人权是外国人发明的，你要人权到外国去要。中国没有人权！</strong><br />
<br />
我懵了，不相信我听到的。愣愣地看着他有几秒钟，发现他不是调侃，不是口误，而是言之凿凿的，信誓旦旦的那种！<br />
<br />
我弱弱地问：中国不是也加入联合国人权公约了吗？<br />
<br />
答：你别跟我提那个，我管不了那个。我是说那个词是外国人发明的。<br />
<br />
我有点恍惚，又弱弱地问：前一段我们不是还发表了一个什么人权发展行动计划了吗？<br />
<br />
对视<br />
<br />
我跟你说，你要是说人权，你跟我说不着，我只管实际损失，比如你丢钱包了；你要说你一丝不挂，你人格被侮辱了，你要人权，那跟我的职责没关系。<br />
<br />
李淑莲：我被一丝不挂跟你没关系？我的人格没有了跟你没关系？<br />
<br />
对，（突然高声：）我说话你听不懂是怎么着？<br />
<br />
我：那你是说老百姓让人侮辱了，就这样了？你们不管？就报案说被侮辱了不行吗？我们来报案，你就给我们记上不行吗？<br />
<br />
不行。记不了。<br />
<br />
记都记不了吗？<br />
<br />
你不是报案钱没了吗？<br />
<br />
也告他们侮辱啊，这个伤害更厉害啊？<br />
<br />
怎么厉害了？有什么实际损失吗？<br />
<br />
一个女人被人赤身裸体拖出室外20多米，这还不是损失吗？对她心里伤害多大啊？<br />
<br />
我说的是实际损失，比如他强奸你了吗？强奸了吗？到底强奸了没有？<br />
<br />
没有。如果强奸我就告他强奸了。<br />
<br />
没强奸我就管不着。<br />
<br />
那我们应该怎样呢？<br />
<br />
不是说了吗，你到法院去起诉去。不是还有中央吗？你可以到中央去上访啊。<br />
<br />
李淑莲:天呐，老百姓还有没有活路了，这么被侮辱都不管。我还怎么活啊。<br />
<br />
你别跟我哭诉，你跟我哭诉什么？你哭什么？告诉你，人格跟我没关系！你甭跟我这哭诉。<br />
<br />
你不是”人民警察”吗？不跟你哭诉跟谁哭诉？<br />
<br />
瞪视<br />
<br />
这中间，进来一个嬉皮笑脸的家伙，李淑莲一看就气得哭了起来：你个不要脸的东西，你怎么来了？都是你们这伙丧尽天良的东西！<br />
<br />
警察：认识啊？<br />
<br />
他是我们那法院的。你为什么叫他来？<br />
<br />
你们别动我电脑啊。<br />
<br />
和那个山东龙口市法院的芦经蠢（音）出去了。<br />
<br />
又是半个多小时。<br />
<br />
我这时才相信了这两位山东大姐的话。<br />
<br />
我也在这时才真的紧张起来。我想起那13个赤膊的大汉。想起暴打姚晶的打手。我想如果他们真的想把李淑莲弄回山东去，肯定不会有好结果的。<br />
<br />
我能坐视不管吗?结论当然是不能。但是，可能要付出代价。<br />
<br />
警察回来了。<br />
<br />
我给朋友打电话说李淑莲今天住我那里。我不相信谁能从我家里把人带走。<br />
<br />
警察让李淑莲到那边去。让我在外面等着。<br />
<br />
我在玻璃门外面等着，他们刚刚进去一个门，马上就听见李淑莲的哭骂声音。<br />
<br />
我开始给110打电话。拨通了，是一个电脑声：你好，这里是110报警台……，循环往复。又打，仍是。<br />
<br />
实在没办法，给老虎庙打了电话。老虎庙说，他在家里拿座机打。<br />
<br />
终于打通了。我说我在右安门派出所，我陪人报案，可是派出所把对方给叫来了。把施害人给叫来了。<br />
<br />
110 女警说，这个得给督察打电话，打到督察，告诉了丰台分局督察的电话，又跟丰台分局督察说一遍，答说，不是得解决问题吗？得抓人不是吗？很正常啊。我说不是抓人，是请他们过来商量，把受害人和施害人放在一间屋子里，如果是经济案件可以商量，这是刑事案件，受害人和施害人怎么能放在一起商量呢？<br />
<br />
什么关系呢？<br />
<br />
上访和截访的。<br />
<br />
哦。那就不一样了。还是得解决问题啊。<br />
<br />
……<br />
<br />
您贵姓？<br />
<br />
姓冯。<br />
<br />
我决定靠自己。<br />
<br />
李淑莲已经从她那间屋子出来站在走廊里，还在哭骂着。<br />
<br />
我隔着玻璃看着她，我知道，我不能让她在我面前被强行拖走。那样的话我永远都无法原谅自己。<br />
<br />
我给老虎庙打电话，说实在不行我就学邓玉娇。无论如何不能让他们从我面前把我的山东老乡抢走。<br />
<br />
我知道我很笨，可是这时也只能如此了。<br />
<br />
李淑莲出来了。警察还拿着她的身份证。她跟警察要身份证。<br />
<br />
说你明天上午10点到派出所来，跟他们负责人谈。<br />
<br />
李淑莲说我不跟他们谈。<br />
<br />
你不是要解决问题吗？怎么这么不配合。<br />
<br />
我说，你怎么能把受害人和施害人放在一间屋子里呢？<br />
<br />
谁说他是施害人了？<br />
<br />
他不是龙口法院的吗？<br />
<br />
那他那天也不在现场。<br />
<br />
可是他们都是一个单位的。就是他们雇佣的打手啊。<br />
<br />
我跟你说了吗？她是报案的，又不是你。你出去！<br />
<br />
我在走廊里给刘晓原律师打了电话，刘律师说，治安处罚条例里有侮辱罪的；身份证是不可以随便扣留的。如果他硬要扣，就让他拿出法律条文来。<br />
<br />
又等了多久？<br />
<br />
终于出来了。问身份证拿到了吗？说拿到了。<br />
<br />
走出右安门派出所大门，已经夜里23点了。给老虎庙打电话说我们马上到他家里去做视频。并请他给我们煮点挂面。（视频请登录老虎庙24小时博客浏览。）<br />
<br />
刚开始做笔录的时候，我还想等走的时候要跟这位年轻的警察说两句话：小伙子，也许你在这个岗位看得太多了，但是不要让职务污染了你的灵魂。人性这个东西也是要小心维护的，一不小心就会丢失的。<br />
<br />
可是，经历了一个晚上的折磨，我已经看都不愿看这个警号为040582的警察了。<br />
<br />
写完上面的文字，我已经不能回看了。不能修改错字，不能改标点符号。我不能看。昨天的头痛持续到今天。<br />
<br />
就像重新经历过一遍。<br />
<br />
我只能关紧门窗，嚎啕大哭。</div>

							
							
							
							
