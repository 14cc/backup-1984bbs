<br />
&nbsp; &nbsp; 本报讯 (记者 邱永芬)忠狗金毛(详见本报连日报道)前日被一别克车主抢走，在部分市民赞“好狗有好报”的同时，亦有人担心金毛是否被人掳为盘中餐。昨日中午，金毛新主人庄小姐致电本报，表示她会善待金毛，并请牵挂它的市民放心。<br />
<br />
&nbsp; &nbsp; 金毛抱走时没挣扎<br />
<br />
&nbsp; &nbsp; 庄小姐住白云区同和的倚绿山庄。前日清晨路上塞车翻看本报“一看到它就觉得它很可怜，不想让它再流浪了”。庄小姐家境殷实，也是爱狗之人，家里养着一只“银狐”犬。当时恰在黄石路附近，就驱车到黄石立交寻找。<br />
<br />
&nbsp; &nbsp; 在摩的司机的帮助下，绕黄石立交兜了多圈后，终于发现金毛就瑟缩在流浪汉留下的席子上睡觉。庄小姐说，金毛很乖，抱它时没有挣扎。而他们也没用绳子绑它。<br />
<br />
&nbsp; &nbsp; 不过，部分为此事扑空的市民心生怨气，在网上发出质疑。庄小姐亦坦承，“我当时动作那么快，也正是担心它被吃狗的人抢去了”。<br />
<br />
