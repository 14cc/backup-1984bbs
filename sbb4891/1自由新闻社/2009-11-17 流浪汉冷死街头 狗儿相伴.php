													<h2>流浪汉冷死街头 狗儿相伴</h2>
						
						
																											<div id="postmessage_331134" class="t_msgfont">核心提示：16日，一名衣衫单薄的流浪汉冷死街头，与之相伴的狗儿蜷在主人原来睡觉的凉席上，神情哀伤。<br />
<br />
<img src="http://i34.tinypic.com/2m2bb15.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
昨日上午，一名衣衫单薄的流浪汉死在黄石立交桥底。殡仪馆人员抬走其遗体时，与之相伴的狗儿亦步亦趋;人员散去，狗儿蜷在主人原来睡觉的凉席上，神情哀伤。两日来，这是第2例流浪汉死亡事件。钟锐钧 邹宇摄<br />
大洋网11月17日报道 昨日上午，斜风冷雨细密地浇着白云区黄石立交桥，一名男子被发现死在这座桥底。当殡仪馆人员抬走他的遗体时，与之相伴的狗亦步亦趋，直至人们对它挥舞扫帚才却步。<br />
大约两个月前，这名中年男子出现在黄石立交公交站点附近的桥底露宿。来自四川的清洁工王女士发现，不久后，一条浅黄色的金毛犬也出现于此。两者自此相依。白天，男子去捡塑料瓶，从垃圾桶里翻吃的，带回一些给金毛。<br />
五六天前，广州起风，该男子转移到立交桥墩底，这里风小些。跟随他的，是一张凉席、一个曾烧过的竹枕，一张绿色的破布，还有揉起来不如一只枕头大的破棉絮。他在睡觉时，金毛也睡在旁边。<br />
附近的街坊通常可见到这名男子，却不知他的姓名。男子只曾说过他是湖南人。<br />
三天前，该男子步履蹒跚爬过桥底水泥栏，到附近的小卖部买水。“只穿了条裤子，上身瘦得只剩一副排骨”，店主老甄记得，男子佝偻着身子，浑身抖索似站立不稳。<br />
“一看就知是有病”，但具体是什么病，老甄说不上来。<br />
在前日中午，四川籍流浪汉老柴来到他附近露宿时，这名男子已不会说话。老柴发现，该男子已不能进食，只能喝些水，“大小便也拉在被子上了”。而那条金毛，依旧在湿被上温着他的身子。<br />
昨日上午9时15分，王女士路过桥底，看见男子仅穿一件脏兮兮的西装，他坐在凉席上浑身发颤地穿裤子。这条裤子同样也是一条单薄的西裤，很脏，系在上面的皮带还比较新。<br />
10时许，王女士再路过时，发现几名流浪汉已经在围着他看。有一名年轻人报了警。而那条金毛，温驯地趴在席子上发抖，偶尔，去舔他的身子。<br />
下午2时40分，殡仪馆的车驶来，金毛不安地走动着。等男子被抬入一袭白床单上时，金毛就贴上去不愿走。直至工作人员挥舞扫帚，它方呜咽着走开。直至车驶远，它仍立在桥底下张望。<br />
“真是条忠心的狗!”围观的群众说。在人员散去后，金毛依旧蜷在凉席上，半眯着眼睛发抖。桥底几米外的高压房里，流浪汉老柴也在被窝里缩成一团。<br />
■新闻链接<br />
前日，男子死在街头<br />
前日上午，站前路和站南路交叉路口位置，一名男子在这里躺卧路边，被人发现时已经没有了呼吸。<br />
“那个人前一天晚上好像就过来了，估计是想找个有热气的地方睡觉。”附近一停车场保安说，头天晚上大概11点的时候，他就看到这个男的来到路口这里。当时光线很差，看不清楚这个人具体年龄和身份。但之后该男子拿出塑料袋在地上摊开，随后就躺了下去。因为男子躺卧的位置刚好是附近地下商场的排风口，天冷的时候里面吹出的风都温度较高，所以经常会有一些流浪人员来这里睡觉。<br />
然而前天清晨时分，在细雨之中有人发现这名男子竟依然躺卧在潮湿的出风口上。一位市民过去查看时，该男子没有任何反应，警察随后赶到现场确认其已经死亡。<br />
■救助动态<br />
暂无市民入住庇护站<br />
从14日夜间起，全市各街道、社区共1300多个庇护避寒站24小时开放，让辖区内有需要的老人、困难人员入住。同时，市流动救助队也派出救助队员上街给流浪者派发御寒物资。从这两天的采访情况来看，记者发现有部分流浪汉不愿意入住救助站。<br />
昨日早上，记者来到西湖路北京街庇护站，这里也是街道的“老年星光之家”。记者看到，站内有20多位老人家在打着麻将“天气冷了，来庇护站打麻将娱乐的老人家比往常增多”。庇护站工作人员介绍，站内有10余个床位提供给有需要的人士入住，房间内还有被褥等御寒物资，此外还有暖炉、热水等供应。但自避寒中心开放后，暂时没有市民晚上来入住，来娱乐的老人家一般下午就回家了。<br />
记者了解到，各庇护中心内的御寒被褥充足，但御寒的衣物比较缺乏。“被褥方面，街道会根据情况随时补给，但御寒衣物则比较少。所以我们希望热心的市民可捐赠一些保暖且有七成新的衣物来。”北京街庇护中心的工作人员说道。海珠区素社街庇护站的工作人员韦女士也表示，庇护站内的御寒衣服不很多，辖区内享受居家养老的老人家们也需要多一些御寒衣物，希望热心的市民能捐赠一些。据悉，有意捐赠衣物或食物的市民，可联系各街道办事处，庇护中心一般都设立在街道属下的“星光老年之家”中。<br />
昨日，市流动救助队继续派出救助队员上街给流浪者派发御寒物资，救助队到了白云区新市、天河区五山路等路段的天桥、立交桥底，向52名流浪者发放了物资，并护送15人入住了救助站。而位于水荫路的救助管理站市区分站也多了流浪者前去入住，昨天就比往日增加了20多人。<br />
而在前日，流动救助专用车来到了仓边路高架桥引桥底为三名露宿者送上御寒物资和食物时，无论如何劝导，露宿者还是继续选择露宿。<br />
