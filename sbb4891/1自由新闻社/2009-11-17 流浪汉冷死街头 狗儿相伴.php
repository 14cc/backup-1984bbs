<br />
&nbsp; &nbsp; 庄小姐抱得金毛后，立马送它到淘金路的宠物医院检查，方知金毛才七八个月大。医生建议她观察一周后，再帮金毛打预防针。<br />
<br />
&nbsp; &nbsp; 初到庄家的金毛，吃了就趴着睡，“可能还在想念它的前主人”。金毛“不瘦”，有五六十斤，庄小姐揣测“前主人(流浪汉)应该也照顾得它蛮好”。<br />
<br />
