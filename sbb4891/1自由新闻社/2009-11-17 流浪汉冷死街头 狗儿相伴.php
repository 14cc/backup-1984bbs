<br />
&nbsp; &nbsp; 庄小姐表示，“请放心，我们一家都是爱狗之人”。而为了纪念它开始的新生活，庄小姐为金毛起了名字叫“初一”。“昨日是初一，我也希望它有新的开始。”庄小姐说。<br />
