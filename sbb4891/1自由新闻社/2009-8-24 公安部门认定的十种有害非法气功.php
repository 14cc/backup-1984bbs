&nbsp; &nbsp; 90年代流行法门：80年代末期，受社会上商业化与自私有理等风气影响，功夫界明显转向，很多所谓大师都冒了出来，但真正有80年代初期那样真实功夫的很少，信息类速效类法门越来越多，很多爱好者练功不久，出了些反应也开始立派传功，由于大家缺乏鉴别力，很多过去被消灭的会道门的法门也借机复出，有的法门形成类似邪教的组织，最终混乱导致了国家的严厉整顿，基本上全部被停顿。国家的政策是正确的，也是必须的。90年代后，功夫界基本以外道旁门泛滥为特点，打击是符合天道的！<br />
&nbsp; &nbsp; 1、中功。中功是开创功夫界速效化商业化快餐功夫的代表性法门，以后众多法门都梦想着采用他的模式走红，却大多没有中功的幸运。中功本身并没有什么高深系统的修炼法门，创始人早年学过几家功法的训练班，然后自己开创中功。其实发展壮大更主要的是靠后面看不见的支撑，速效是速效，功能也出了不少，但有个共同特点，那功夫是糊涂的，不明白怎么来的，非常被动的。中功主要锻炼部分是一部功，但真正起作用的还是背后那看不见的支撑，相信明白人知道怎么回事。一个法门如果把练功者的私欲激发出来，这样的法门来历可想而知。中功对90年代法门的影响是最大的，整个90年代练功者，大多受到影响，欲望性很强，出偏的也很多。<br />
&nbsp; &nbsp; 2、九宫八卦功法。这个法门和传人于光生先生本来没多大名气，但由于法门内出了个大名鼎鼎的轮子李，大家才注意这个法门起来。九宫八卦功法又称璇玑科学，有自己一套独特的理论与修炼方法，独树一帜，理论与修法也是系统的，缘分对路不妨学习研究，是不同于丹道修炼的另外一种修炼模式。不过任何法门都是万千法门之一种，过高的宣传与自我评价，都是不客观的，古师说依法不依人，是至理。<br />
&nbsp; &nbsp; 3、慧莲功。这个法门属于佛家，包括净土和三密法门，功法正统，出来也比较早，但在90年代依然很有名，在传播初期还是不错的，在90年代影响大的就是对卫星发射的预测和奥运会的预测，结果一对一错，折戟在奥运预测。据知情人透露陈先生后期就很少练功了……等等，毕竟是传闻，各自去判断即可。修佛者以佛为师，修行路上步步魔障，后期及未来的路走到哪，还是靠自己，似乎21世纪又有了消息，与之前的法门区别很大，不在这里多说了。<br />
&nbsp; &nbsp; 4、鹤翔桩。这个法门是80年代峨眉临济宗的学生赵先生所创编，来源于峨眉十二桩，其中的自发功治疗效果不错，但也有不少出偏的，主要还是创编者对自发功理法不明，自身修为也不足所造成。其与同门庞先生的恩怨江湖尽知。<br />
&nbsp; &nbsp; 5、智能功。这是与鹤翔桩的赵先生同门的庞先生所创编，其中原委不说。智能功同样来源峨眉十二桩，90年代初期影响很大，全国很多锻炼者，养生治疗效果也是不错的，可见峨眉千年古法，即使后人改编，也是威力不小。<br />
&nbsp; &nbsp; 6、玄灵功。这个法门是个争议性很大的法门。熊厚金先生有家传，早年也与弟弟一起学过几家功法，后多次去四川严新家乡，受过严新先生点化，是有实际修为的。后与武汉东升气功研究所合作，出来传功。早期教材是借鉴了台湾青城派灵仙宗卢胜彦先生的启灵炼元神等法门的，一些章节故事也未加处理。天盘罡斗部分则据说来自龙门王力平先生的学生。这样传承上与法脉保护就出现问题。事情不断，造成出偏者较多，法门也分裂。3年闭关，98年该师推出自成体系的“佛光心灯”系列法门，算有了自己的功法体系，但却依然坚持自称金丹大道，太上法脉，早期特别推出虚拟的“青龙山人”则不再提及。21世纪后传人去到德国，转入推广《德道经》，以此经对内指导学生修炼，对外推广诵读，转型工作基本完成。<br />
&nbsp; &nbsp; 7、玄德功。名义上是熊厚音先生（厚金之弟弟）主持，实际带修者是其妻殷女士，内部称做“元君”老师的。殷女士据说是看到严新先生一个带功的处方，而进入练功状态的。此后进入一种自发的练功状态，读了一些严新先生的书。与玄灵分裂后，玄德主要以她功中所见的一个老人”师父”为师，所有的练功人都以这个老人为师。内部暗示，这个老人就是老子化身，其功学员都是太上弟子，学生中对此崇拜，是修炼的主要动力。对照玄灵，都是对太上的崇拜，均自称正统老子门生，事实如何，明者自知，不便评论。<br />
&nbsp; &nbsp; 8、香功。这也是比较流行的法门，该法门流传大江南北，城市农村，效果也是不错的，传人说是来源于佛教禅宗。此法门动作简单，但是的确有效果。说穿了动作也是符号，也可以起到相应的沟通与采取锻炼的作用，并非一些朋友认为的那么简单。至于个人的行为，还是依法不依人，客观评价法门，与人品无关。<br />
&nbsp; &nbsp; 9、地丹法。这个法门是郝俊业先生所教授，据报道实践效果不错。传闻其从学于东北某道长，但观其教材，对三元丹法的理解显然错误，与中华道家对天地人的定义相差太远，三元丹法的理论始终都是继承传统的道家理论的。从此点看其从学某道长的说法，是有人要借此做文章，未必是真，不足为凭。而其修炼模式也与西藏某类观想型的性修炼类似，相对真正的道家此类修法，太显粗糙，而其传功采用了中黄复制的手法，除非获得他的复制手法，别人是难以去传授此法的。<br />
&nbsp; &nbsp; 10、轮子李。这是整个90年代的颠峰之作，90年代功夫界的私欲与迷信在轮子李的法门里得到了颠峰发展，以超越佛的境界来放大练功者的私欲，使法门得以迅猛发展。本来李仅仅是九宫八卦法门里的学生，去新加坡回来后，创编FA轮*功，以高层次为诱饵，使很多对正统修炼了解不多的人上钩。迅速发展成最大的组织，直到与政府对抗，被定性为邪教而取缔，同时大多数流行法门遭到打击，结束了烘烘烈烈的90年代外道为主流，正法式很微的大表演。<br />
&nbsp; &nbsp; 整个90年代，随着社会的道德沦丧，功夫界可以用乌烟瘴气来形容。主流是追求心空前，速效法门，高级法门漫天飞，成仙成佛被人们所认识并追求，在气功的形式被国家打压后，为催生21世纪初期到如今的宗教回潮，丹道的升温打下了很大的前期基础。也为这次的宗教兴起，丹道的异化，唱响了一个私欲主导的不好的前奏，宗教界与丹道界如今的混乱，不能不说不与此有关。<br />
&nbsp; &nbsp; 上世纪90年代末，国家对混乱的气功界采取了全盘打压的政策，是十分正确的，虽然少数正法也受到影响，但毕竟使众多的伪法旁门外道受到了重大打击，而暂时消退。但人的私欲是不会消失的，当年很多练功者摇身一变，渗透进宗教界和丹道学术界。开始以佛教道教和丹道的名义死灰复燃。很多地方的宗教协会的发起人就是当年某些功法的当地骨干，当然大多数都是以居士身份，他们是舍不得出家的。宗教表面上繁荣了，但却带有了越来越多当年气功界的私欲追求，一些人把当年练功的心态带进了学佛学道中。因为在他们看来，当初的气功太低级了，如今学佛学道才更高级，心态与当年学高级功法是没有任何区别的。另一部分转入丹道，看古书，写文章。以丹道来包装，换了身份。他们共同特点就是，对当年的气功界的法门，不做分析的一概嘲笑：气功嘛，低层次，实际就是他们已经完成了转换有了新的马甲，要急着划清界限，抬高自己。而实际上这些人大多是当年跟着练功的爱好者为多，实际水平并不如八九十年代传功的老师们，不过嘴皮子功夫确实了得的。号称修佛修道的，眼高于顶，却没有古师的心性与能力。号称修丹学仙的，抱着明清以来秀才书，实际修为，养生都难以达到，说穿了是根本就没有得到真传，不过写书本事还是不错的。可惜功夫不是靠文字来展现的，90年代末以来，打着丹道旗号的一些江湖人士，号称真正丹道次第，三家龙虎等等，实际无外乎明清以来会道门伪丹伪法的改头换面，在后学《会道门所谓丹法与正统道家丹法的区别》一文有详细论述。如果要学丹道，入门还是从前面介绍的80年代那几位正统师傅传授的基础法门入手，心性德行到了，自然会遇到真师，真法难遇而易成，丹道真诀从来不是可以写在书本的，那口传心授的玄妙，是忽悠不来的。无论是宗教还是丹道，这个时代都带有上世纪90年代鲜明的烙印，而本世纪兴起的法术热风水热等也同样如此，网络也成为一个重要传播途径，产生一批网络大师，由于相对敏感，不做评论，但整体素质与能力水平，客观的说，是不如上世纪80年代的师傅们的。<br />
&nbsp; &nbsp; 此文只是闲聊的话题整理，不是学术著作，只是一时的看法。由于后学修为有限，阅历不深。各法门藏龙卧虎，有理解不当之处请海涵。</div>

							
							
							
							
