																											<div id="postmessage_220706" class="t_msgfont">雪涛点评当代流行气功<br />
此文作者雪涛，应为道家龙门派王力平先生门下弟子。于此<br />
<br />
文中观点或微有不洽合之处，然特广涉百家，独具卓识，甚<br />
<br />
堪一观也。<br />
－－－－－－－－－－－－－－－－－－－－－－－－－－<br />
<br />
－－－－－－－－－－<br />
1、郭林新气功郭林新气功实在是80年代气功浪潮的奠基者。<br />
<br />
郭林在70年代末的教学活动也开创了新时代气功教学的新形<br />
<br />
式，而她在功法上的大胆创新，对古典方法的大胆革新也是<br />
<br />
气功以新的功效首先获得一部分人的认可（一批癌症的其他<br />
<br />
绝症患者）。后来许多**高唱的所谓“创编”一词实际上始<br />
<br />
于郭林。郭林新气功对于癌症却有相当的实效，现今许多城<br />
<br />
市的抗癌俱乐部都是以郭林新气功和后来由此发展的气功自<br />
<br />
控疗法为主的。可以这么说，郭林是新中国现代气功的先行<br />
<br />
者和奠基者。<br />
2、 气功自控疗法来源于郭林新气功，并没有多大的发展，<br />
<br />
后来却要攀附钟吕丹道的重要典籍《灵宝毕法》，成为有识<br />
<br />
之士的笑谈。<br />
3、内养功刘贵珍在五十年代即在北戴河疗养院开始对健身气<br />
<br />
功的探索，但是，多半受益者都是中共的高干和英雄模范，<br />
<br />
没有在平民中广泛流传，这就是我为什么不把他列为现代气<br />
<br />
功的奠基者的原因。不过，由于他的功法对象的特殊性，安<br />
<br />
全度是相当高的，用于健身还是相当不错的，可惜现在人们<br />
<br />
经过**门的熏陶，都不没有功能和不能带功的功法视作低级<br />
<br />
功法。保定的张觉民就是在受益后开始对漆工的探索的。<br />
4、 鹤翔庄 80年代真正掀起气功浪潮的是赵金香的鹤翔庄。<br />
<br />
实际上，鹤翔庄是庞明（庞鹤鸣）创编的，但是，赵金香教<br />
<br />
功时，庞明还在监狱里面，为避免一些麻烦，就没有说明是<br />
<br />
谁创编的。赵金香的功绩是建立了后来为大家所纷纷效仿的<br />
<br />
金字塔式传功体系，有效的保证了功法的迅猛发展。后来，<br />
<br />
赵金香又继续创编了二部功“自在气功 ”，是一个大杂烩，<br />
<br />
佛道密都有，却都不对，按其修炼健身还可以，修行就是笑<br />
<br />
话了。鹤翔庄的自发动功不是传统的气满自动，而是采用了<br />
<br />
意念诱导的方法，这就导致了后来众多的出偏人群。群众对<br />
<br />
出偏的认识始于鹤翔庄。后来，鹤翔庄对传功对象有了一定<br />
<br />
的限制，如癔症患者和有家族精神病史的人不传，但仍然有<br />
<br />
较高的出偏率。我的一个同学就是这样的。<br />
5、形神庄（智能气功）庞明后来自己开始传功是，鹤翔庄已<br />
<br />
经红透中国了，他只好自己另外创编了形神庄，把原来的鹤<br />
<br />
翔庄缩编为“捧气灌顶法”，并吸收了鹤翔庄传功过程中出<br />
<br />
偏较多的教训，站桩采用了双脚内扣不易出现自发动功的三<br />
<br />
心并站桩。但是他的功法为了在广泛传功中不出现难以解决<br />
<br />
的问题，对有些古法做了较多改进，比如，他不练下元，不<br />
<br />
强调静功，认为动功可以开智，实际上这是不对的。他还在<br />
<br />
智能气功的练习者中发起了混元气的应用研究，试图使其介<br />
<br />
入现代科学的研究，可谓是聪明一世，糊涂一时。在明眼人<br />
<br />
看来，这不过是气功界土法上马大炼钢铁的一次气功大跃进<br />
<br />
而已。气功的核心价值在于人类自己身心的修炼，而非对外<br />
<br />
在的追求。说白了，这一切包裹是深层潜意识**利意识的不<br />
<br />
经意表现而已。<br />
6、 动意功石家庄郭志辰的动意功也是来源于庞明，很多人<br />
<br />
可能不太理解。当然，郭志辰有他自己的发展，但就其最初<br />
<br />
的理论来源确实如此。功法是不错的，但也就是健身而已，<br />
<br />
不能作为修行的功法来练。<br />
7、少林内劲一指禅很不错的一个传统功法，用于健身和武术<br />
<br />
内功的筑基都是不错的。我家有个邻居，曾经动过颅腔手术<br />
<br />
，但是后来学了此功，十多年如一日，到现在，谁都羡慕他<br />
<br />
的身体，就是大冬天的，60多岁了，也从来不感冒。这个功<br />
<br />
法的奥妙只有练过了才知道，呵呵！这可是个苦功法。<br />
8、 空劲气功空劲气功来源于少林内劲一指禅，这是谁都知<br />
<br />
道的，但是，后来黄甚至争正宗地位，就有些不合适了。这<br />
<br />
也是中国人的老毛病了。<br />
9、大雁气功始传自杨梅君老太太，是道家昆仑派功法，安全<br />
<br />
系统，基础牢固，是值得推广的好功法。杨老至今的长寿是<br />
<br />
这个功法最好的广告。唯一的缺点是前后64式动作较多，初<br />
<br />
学者不太容易掌握，不过，红旗下成长的我们都有过体育的<br />
<br />
基础，还不太难。当初我妈学此功时，可是费了九牛二虎之<br />
<br />
力。<br />
10、真气运行法：此功在周天类功法中影响较大，李少波的<br />
<br />
功劳是把具体方法分解得比较明细，便于操作，从而保证了<br />
<br />
成功率。但他解决得比较好的是获得气感及聚气的问题，对<br />
<br />
冲关尤其是玉枕关和下昆仑的问题没有好的解决办法，导致<br />
<br />
很多自己练习此功的人出现坎头气的偏差。此功是典型的中<br />
<br />
医气功，通的是经络周天，而费丹道筑基所要求的脉路周天<br />
<br />
，所以用来健身尚可，不能作为丹道的筑基功。<br />
11、元极功这是一个有相当特色的功法，体系完整，张后来<br />
<br />
编的动功纯属画蛇添足，是为了扩大练功人群而设。因为出<br />
<br />
自会道门，功法传人对于正统的经史子集并不熟悉，把所谓<br />
<br />
的元极图当成自家的宝贝。实际上，那就是易学史上著名的<br />
<br />
皇极图。<br />
12、慧莲功虽然不是最高的，但确实是正统的内地佛门功法<br />
<br />
，其特色在法界大定、十六关和三密气功。陈林峰在预测<br />
<br />
2000年奥运会失败后销声匿迹。其实，他的预测也不算完全<br />
<br />
失败，我猜想，主要是对功中图象的解说和分析错误所致，<br />
<br />
或者说是时空没有把握好。<br />
13、香功这是一个令人哭笑不得的功法。它把气功的简单化<br />
<br />
导致了一群群老太太一边挥舞着双手一边张家长李家短的街<br />
<br />
头奇观，简直就是糟蹋气功。我看见这情景时，叹息：“气<br />
<br />
功怎么变成这样了？气功快完了。”他们最喜欢炫耀的是田<br />
<br />
组场带功时的各种奇光异彩的照片，以次证明田的高功夫。<br />
<br />
嗨！这样的照片我也有，可是不能证明我是**呀！<br />
14、严新气功严新是带功报告的首创者，是把气功热推想最<br />
<br />
高潮的始作俑者。他的功法很独特，基本以观想为主，场景<br />
<br />
