																											<div id="postmessage_220543" class="t_msgfont">不知道人点评20年江湖流行法门<br />
<br />
转自<a href="http://www.zhongyifu.net.cn/bbs/dispbbs.php?boardid=3&amp;id=1001&amp;page=1" target="_blank">http://www.zhongyifu.net.cn/bbs/ ... ;id=1001&amp;page=1</a><br />
<br />
古人云：<br />
&nbsp; &nbsp; 滚滚长江东逝水 浪花淘尽英雄 是非成败转头空 青山依旧在 几度夕阳红<br />
&nbsp; &nbsp; 白发渔樵江楮上 惯看秋月春风 一壶浊酒喜相逢 古今多少事 都付笑谈中<br />
&nbsp; &nbsp; 一首词述尽了世事沧桑，潮起潮落。上世纪八十年代至今，20多年过去了，从当年气功开始，中华功夫界，江湖风云变换，你方唱罢我登场，功法形形色色，各展风骚，令人眼花缭乱，起落延绵。发展到如今的网络江湖，多少风云人物与法门，都在用自己的功夫与行为书写了自己的历史。后学由于特殊原因对此20多年江湖略有了解，近距离接触了诸般法门与大师。某日与诸位友人笑谈，品茶闲谈20年江湖风云，记录如下，别无他意，只是泛泛而谈，不准确处敬请包涵，此非兵器谱，不涉及排名，介绍的法门大体按照时间顺序，想到写到，无须多想。此点说明在先。<br />
&nbsp; &nbsp; 上世纪八十年代及以前：八十年代之前以及前期，出山的师傅大多是名门正派的传人弟子，功法传统而实在，效果显著，老师品行也相对不错。从80年代后期开始，商业化味道逐步浓厚，到了90年代，基本功夫界被商业化取代，真师就很少了。<br />
&nbsp; &nbsp; 1、严新生命科技。当年也被称做严新气功。严新先生是八十年代最有影响力的出山师傅。以带功报告的方式重现历史上曾经的高功师傅大规模普传修行法门的场面。其人功夫品行和功法，都是上乘上品。报告中把中华传统内功修炼的精华用现代语言讲解出来，为现代人修炼传统内功提供了标准性原则，他重点提倡的“重德是传统内功修炼中的高技术”，确实是正统修炼的重点技术，是进入正法修炼必须高度重视的。严新先生在国内传授了很多方法，大家比较熟悉的就有几十种。这些法门表面看起来互不相关，不系统，而在明眼人眼里，却是系统系很强，方向性也很强。有一根主线在引导。从静坐入门到童子九步功九心轮养生法健美功长寿功内丹十八法等，实际上深入下去，会发现很多的内在联系。严新先生的真正师承是个秘密，海灯法师是他的武术师父，其他20几个民间师傅都有所学习，但其根本的师父，却是小时侯那“老山伯”的老师傅。在某次在家乡谈话提过一点。严新先生传授的法门是层次很高的，无论从丹道层面还是从法术层面，都是很高级的法门，看不出高在哪，就要反思自己的智慧与福德了，我可是不会轻易赞叹一个人的，呵呵。严新先生传授的“内丹十八法”似乎来源于道教南宗白玉蟾祖师《海琼传道集》的“丹法参同十九诀”的前十八诀，上阳子的《金丹大要》里有引用，名称就是“内丹十八法”从这点看，严新先生可能与道家南宗有关――当然这只是猜测。南宗现在已经被世俗秀才研究的面目全非，不好多说了。总之严新先生传授的法门，是最适合当代人没有遇到真师的人自修的入门法门，按照重德为本的原则，自学先生同意普传的童子长寿九步功，您会发现您的选择比花了很多银子，比那些所谓丹道道学大师们的东西好的多。其实目前很多丹道法门，按严新先生某次报告里说的，“在那什么都不想，晃兮糊兮的在那，那不叫练功，只能叫很好的休息”（非原话，大意如此）。那样子能有象样的成就也奇怪了，还是那句话，修丹道的如果不理解严新先生，那么您的丹道就需要检讨了。后学郑重推荐严新先生传授的法门！不过这个法门是要好好修德的，实在的修德的，如果目的不大好的，学了没好处效果也不好，传统正道法门就是这样，没办法啊。如果抱着名利等其他想法，不妨学下旁门的法术，银子用到了，就可以学到的啦！<br />
&nbsp; &nbsp; 2、灵宝通智能内功术。这个法门是龙门派18代王力平先生传授的法门。包括三功九法。其中核心的三仙功，是以《灵宝毕法》为经典而修炼的。修炼过程结合了“太乙金华宗旨”。考察功法特点，与钟祖吕祖的《钟吕传道集》《灵宝毕法》邱祖的《大丹直指》是完全相合的，对五行颠倒的重视得到体现，应是钟吕法脉嫡传的某一支。另外还有个好东西就是人与星阵的对练，这可是当年很少有人传授的道家修炼法门啊，星罡的作用，不用我说了吧，那是绝学，对丹道修炼的作用，行家自然明白的。这个法门应该属于吕祖早期传授弟子的法门，纯粹的“我命由我不由天的”技术性操作法门，只要刻苦按照方法锻炼，就可以获得成果，修出东西来，自己完全做主，成仙成佛成魔修出来后都可以自己做主！这个是与吕祖参黄龙禅师后觉悟的“我命由我亦由天”是有所区别的。吕祖后期法门，是初期就与天地相应，要靠修德获得天助，不是单纯靠方法就可以成功的，联系严新得到老师傅师傅传授的法门，能悟出点什么不？呵呵。王先生的法门，真实可靠，精华在初期对五行颠倒的运用，那是丹道产生真种的前提，不象世俗丹法在那初期就无为，不清而妄图求静，结果肾水旺盛阳举就在那采炼，弄出“水火煮空铛”的笑话，没经过五行颠倒，哪来的真种子和药物？水只是溶剂，烧干了连养生都做不到的，看看世俗丹家的身体状况就清楚了。不过灵宝仙法，好是好，就是一个苦，炼起来特苦，盘坐要求高！要有吃大苦精神才练的出好东西。懒汉就不要学了。<br />
&nbsp; &nbsp; 3、天山派内功。这是陈伟先生传授出来的内功法门。据陈先生介绍，天山派的形成至今已有八百多年的历史，它始创于南宋光宗绍熙元年（1190年）。天山派祖师杨继明系北宋名将杨继业之后，幼年时在太行山师从晋葛洪真人所创丹鼎派的第九代传人王世光，后因南宋衰落，战事纷乱，遂远遁西域（今新疆天山山脉博格达山区）隐居，习练武功，修真参玄。形成“天山派”的是祖师杨继明之子杨延天与当时同隐天山的道教神霄派王文卿之徒萨守坚。他们结伴同修，一起创派并制规，因地而名为“天山派”。其丹道很有特色，可以看到宋代丹法与现代某些丹客的巨大区别。陈先生的功夫真实不虚，后学曾有朋友亲身体验过。其功法由经络起修逐步深化到脉到脏腑等，层次分明，内外兼修，也是传统道家法门里比较稳妥可靠的法门。<br />
&nbsp; &nbsp; 4、峨眉临济宗功法。此法门最早由60年代周潜川先生公开了部分法门比如峨眉十二桩、清静归一法等，当年能传授的法门，都是很古老很正统的法门，难得的千年峨眉古法。周先生的弟子巨赞法师曾经传授过简化峨眉桩，也学修密宗法门，可惜修炼中出现了偏差，过早去世，峨眉绝学很多没有传下来。因此气功大潮时期，巨赞法师的传人傅伟中先生面对光怪离路的功夫界，居然仿照某法门，把峨眉法弄成了速成八部，抱着宝贝羡慕外人。其实千年峨眉，法门根基深厚，祖师白云禅师是继承了道教上清派内功与法术，结合禅宗心法创立的。内容博大精深。好好修炼，效果自然很宏大的，只是师傅不在了，需要花费些时间罢了。即便因巨赞法师早逝，对法门造成重大损失，但因为千年底蕴，还是造就了一大批人才，后期著名的赵金香的“鹤翔桩”、庞明&quot;的“智能功”、王某的“弘德正道功”、刘某的“东方睡功”，都是峨眉门下，改头换面所创编。如今峨眉传人一灯先生，后学90年代曾经有过会面，单衣过冬是真实不虚的，据说他是带艺投师拜在傅先生门下，目前在某县城隐居。这个法门是很正统很传统的法门，很不错的。<br />
&nbsp; &nbsp; 5、裴锡荣先生武当内功。裴老先生是俺家乡出来的高人，武当功夫真实不虚，门下人才不少，比如著名的张元明先生也是师出裴老门下。裴先生的武当功夫，武道结合，套路与静修丹道结合，五雷掌内功有自己的特色。其法术功夫很少外显。裴老很多法门都公开在杂志与书籍，普通的部分可以自学。好好锻炼，养生有余，内功也会有成，至少比那些忽悠丹道的效果会好的。不妨练下对比就知道了。<br />
&nbsp; &nbsp; 6、李兆生先生的武当真元内功。这是个流传比较广泛的法门，也属于比较传统的武道法门。法门系统，效果稳定，好好锻炼是可以获得很好的效果的。目前传播的属于基础内功层次，高层次的内功还没看到，其金气大周天的文章是有东西的，不是普通丹客的文章。这个法门传播广泛，自然不免人龙混杂，出现一些名利弟子，对法门声誉造成不良影响，是值得深思的。<br />
&nbsp; &nbsp; 7、禅密功。这个法门在80年代是很有影响的。培养出不少的人才，90年代后一些出来的所谓大师，当年有些就是学过这个法门的。这个法门的锻炼效果是不错的，刘汉文先生也是有实际水平的。功法不复杂，也较少玄虚，适合大众锻炼。<br />
&nbsp; &nbsp; 8、太极五行功。这是何先生学自一位道长的道家法门，似乎来源于太极门。该功法效果不错，有专门针对癌症的锻炼方法。该法门基础阶段后的正式修炼就要经过太极门特有的“下种”，进入一种自动化锻炼，效果明显。<br />
&nbsp; &nbsp; 9、陆锦川先生传授的道家太极门功法。陆锦川，又名仿佛先生。集翰墨、道功和武技于一体的旧学世家。精通佛学，少年便得道家太极门真传。多年来行医、演讲、著书，形成独特的风格。仿佛先生已先后出版的著述有：《气道》、《养生修真证道弘典》、《九灵针经&#8226;气道针经》、《中医望诊相法》、《仿佛谈道录》等，是系统介绍道家太极门修炼法门的前辈。其对修炼的很多论述被后来一些人所发挥，比如“下种”就后来曾经一度流行过。<br />
&nbsp; &nbsp; 10、少林内劲一指禅系列。又叫罗汉神功，据称南少林传下的功法最初由上海的阕阿水先生传授到社会，80年代后现了几个知名的师傅，比如王瑞亭等。空劲气功与此法门也有渊源。这个法门最大特点是扳指法的运用，可以锻炼内功治疗疾病，激发罗汉神拳等。健身效果明显，也适合自学。以养生健身为目标的，可以放心自学的，即使想深修炼，也可以以此打下坚实的内功基础的。<br />
&nbsp; &nbsp; 11、静动功。这个法门传授很早，60年代的胡耀贞前辈曾经公开传授过，医疗养生效果明显。该功法锻炼先天气，静中求动而出现自发功，来治疗疾病，锻炼内功，可以出现五禽动作。深化修炼可以通经脉周天，如果有师傅点拨，则可进一步炼丹修道。80年代梁士锋先生的自发五禽戏动功，应该与此法门有关，合并介绍。这个法门把握住了初期道家修炼的关键，是很好的道家入门功法，五行颠倒，龙虎交媾等法诀都会自然的修证出来。所谓内行看门道，这个法门的自发功不是外来信息控制，是自我之神支配。只要有明白人指点，是安全的。<br />
&nbsp; &nbsp; 12、元极功。这个法门据传人介绍来源于太一道，功法体系比较完整，属于传统法门。修炼者的效果也是不错的。不过，无论锻炼还是传播，要合法才是基础。<br />
&nbsp; &nbsp; 13、大自然中心功。这是个非常典型的民间巫的练功方式，当时影响很大，特点是在接通灵界的基础上纯粹无为的锻炼，实际把自己身心交给了外在的所谓“师傅”，这是民间巫最典型的特点，这类法门对后期有很大影响，很多追求速效的人喜欢。这类自发功与正统道家的自发功区别很大的，道家属于“清而后静”，其动乃自己之神把握。这种外求的法门，是要小心的。出偏的人比较多。<br />
&nbsp; &nbsp; 14、大雁气功。这个法门以导引动功为主。传人介绍属于道家昆仑派，确实具有养生健身效果，传人也很长寿，导引术自古属于一种锻炼法门，不可小看，同样具备很高深的哲理，这是很多轻视导引法门的同道要注意的。实践效果才是硬道理。<br />
&nbsp; &nbsp; 15、中华药功。这个法门是湖南龙怡女士传授的，是与法术结合的练功法门，是功夫界比较早推出符咒法术的门派，据说后来名扬天下的传授莲花大法的魏令一先生也是出自此门。此法门是比较系统的民间法术门派，效果也很明显。是民间道法真传，只是当时识货的人可惜不多。<br />
&nbsp; &nbsp; 16、真气运行法。这是属于典型的养生内功法门，是很实在很塌实的养生内功锻炼方法，效果显著，不弄玄虚，立足于医疗与养生则是好法，如果拔高弄玄就与实际不符合了。<br />
&nbsp; &nbsp; 整个80年代，虽然也是鱼龙混杂，但还是以真法为主为多，整体上是稳定的实在的，也是可以学到真东西的。<br />
