													<h2>CCTV2《今日观察》：谷歌的无礼颠覆了什么？</h2>
						
						
																											<div id="postmessage_294180" class="t_msgfont">ugmbbc发布于 2009-10-21 12:59:37|14019 次阅读 字体：大 小 打印预览<br />
<br />
短短的五年当中，全世界近千万种图书被收录到谷歌的数字图书馆当中，仅仅在中国就至少有570位中国作家的17922种作品在他们毫不知情的情况下 被收录，而且这个数字还在不断地扩大。面对作家们的集体声讨，谷歌在近期就提出了一个和解方案，但是这个方案一出台就引发了众多著作人的不满。究竟这个数 字图书馆计划是怎么回事？为什么会引发如此大的争议和纠纷呢？那么我们应该如何面对互联网时代的数字版权？央视财经频道主持人王小丫和著名财经评论员马光远、刘戈共同评论。<br />
<br />
<br />
　　海量作品被悄悄上网，谷歌数字图书馆版权风波成众矢之的，和解协议再度引发争议。<br />
<br />
　　张抗抗：著作权理应得到尊重<br />
<br />
　　（中国作家协会副主席、著名作家 《今日观察》特邀评论员）<br />
<br />
　　谷歌在扫描之前完全没有经过我的同意，事后也从来没有跟我接触过。尽管文化资源共享，是让更多的人得到知识和文化。这个并没有错，但是有一个前提，就是一个法律意识的前提，就是著作权应当得到尊重的一个前提。<br />
<br />
　　张洪波：谷歌无视著作权 完全颠覆版权付费模式<br />
<br />
　　（中国文字著作权协会副总干事 《今日观察》特邀评论员）<br />
<br />
　　先授权，并且付费，然后再去使用，谷歌公司已经完全颠覆了这样的一种方式。<br />
<br />
　　刘戈：谷歌侵犯著作权 不合理赔偿惹众怒<br />
<br />
　　（《今日观察》评论员）<br />
<br />
　　其实我们作家的这种愤怒特别容易理解，这件事相当于有一大块地，长满了庄稼，现在成熟了，有一个人过来不跟任何人打招呼，就把庄稼给全割了。然 后告诉我，如果你三天之内来找我，我就给你60块钱，我把这些庄稼卖了以后，就将其中63%的收益给你。干不干？如果你不干的话，好庄稼你自己拿回去，这 是一个办法；或者还有很多人可能压根就不知道这个消息，或者没有在我限定的时间内来进行交涉，那么这就算作你已经放弃你的权利。<br />
<br />
　　目前存在的盗版行为，大家都是司空见惯的。但谷歌作为一个大的机构，用这样一种方式来对待著作权人和对待版权，大家的愤怒是自然而然的。孔乙己有一个理论，就是“读书人偷书不算偷”。谷歌现在就是一种新的理论，因为我为读书人着想，所以我把书偷偷地复印了也不算偷?<br />
<br />
　　马光远：谷歌侵权“理直气壮” 和解协议是“霸王条款”<br />
<br />
　　（《今日观察》 评论员）<br />
<br />
　　谷歌的确很傲慢，在著作权领域，有着对著作权人基本权利的尊重，比如说告知权，比如说付他的报酬权利，这些都是最基本的权利。谷歌在这两个方面，不仅仅对中国作家，可能对全球作家，他都采取一视同仁的态度，这里涉及到570位中国作家，这个事其实已经发生了很久。<br />
<br />
　　2008年，在美国，谷歌已经达成了和解协议，但中国570位作家，可能根本不知道自己的作品是否被谷歌扫描了，也不知道自己作品在谷歌图书馆 里面处于一个什么样的地位，是整个事件一无所知。美国司法部对谷歌与美国作家协会以及著作权协会达成的60美金和解协议表示不满，认为这个协议可能会损害 相关著作人的权利。美国方面表达不满以后，才引起我们的关注，中国文字著作权协会才把从+其他国际组织转过来的一份邮件，放在自己的网上，让中国作家上网 去查自己的作品是否被扫描。如果被扫描，就提出权利的申请。一个完全没有针对中国作家的和解协议，在中国整个著作权伦理里是不允许的。现在引起这么大的全 球争议，跟谷歌本身的做法有很大关系。<br />
<br />
　　刘戈：免费不等于公益 谷歌违反商业伦理<br />
<br />
　　（《今日观察》评论员）<br />
<br />
　　免费不等于公益。现在免费，不等于以后永远免费下去，而且免费本身就是一种商业模式，即使谷歌真的在很长时间里面不收费，没有任何的盈利手段，但是程序也要符合常见的，传统的一种商业伦理.也就是说你拿人家的东西要先跟人家打招呼。<br />
<br />
　　就是完全在网络时代，传统的商业基本伦理，还是一定要是遵守的。此外，谷歌的这种做法相当于把成本转 嫁给了广大的著作权所有人，让这些著作权所有人主动来找谷歌，大大降低了成本，这是违反商业伦理的。<br />
<br />
　　马光远：谷歌始终是商业机构 免费模式藏隐患<br />
<br />
　　（《今日观察》 评论员）<br />
<br />
　　谷歌毕竟是一家商业机构，最终他还是要赚钱的，尽管他目前所讲的免费听起来很美，但是美的让人担心。<br />
<br />
　　第一，谷歌本身是一家美国公司，它的主流价值和文化主要是美国文化，谷歌在筛选图书和排列组合上，为了自己赚钱的目的推出一些东西的时候，他主要给予的是英美文化，其他国家的文化会处于次要位置。如果谷歌处于垄断地位，那么这种作用就会更为明显。<br />
<br />
　　第二，谷歌对作者的不尊重已经引起了大家的担忧。当谷歌一家独大以后，颠覆了以前的传统的出版模式后，会对包括出版商在内的其他机构造成很大影响，在谷歌真正做大以后，这种力量会真正显示出来，现在他已经在显示力量。<br />
<br />
　　第三，谷歌网上图书馆本身是对原先传统出版模式的一种颠覆。这种颠覆本身也意味着一种利益的分割，不仅仅是作者跟谷歌之间利益的分割，还意味着 谷歌跟其他出版商之间的利益分割，这种利益的博弈是非常激烈的。当谷歌真的实现自身的理想图景以后，其他出版商很大一部分利润就要进入谷歌的腰包。所以不 同的层面、不同的人站在不同角度，对谷歌这种担心一点都不多余。<br />
<br />
　　闷声做大数字图书馆，谷歌频遭全球出版界抗议，网络时代，中国出版产业如何突围和发展？<br />
<br />
　　马光远：应尽快确定著作权权利的边界和权利的模式<br />
<br />
　　（《今日观察》特约评论员）<br />
<br />
　　德国总理默克尔表示德国政府会尽全力保护德国作者的权利。目前，就中国的作者而言，中国文字著作权协会正在做这方面的工作，但仅仅靠一个协会还 不够，我们首先还应该表明我们的立场，而且这个事情本身，的确不仅仅是谷歌与著作权人的权益之争，他可能会给未来确定一个权利的边界，权利的模式。如果此 次全球不和谷歌争取这份权利以及划分一个很好的权利边界的话，那么未来谷歌可能会成为这个领域的垄断者。<br />
<br />
　　刘戈：中国应推进现有版权制度 适应网络时代的要求<br />
<br />
　　（《今日观察》 评论员）<br />
<br />
　　谷歌侵权事件让我们反思。目前出版界通行的版权方式是不是适合于现在网络的大环境？从将来来看，网络出版可能在一定程度上，甚至可能会在不久的 将来取代图书出版的过程。所以现在要研究，中国是不是可以把现有的版权制度做一种推进，让它更适合于网络时代的要求。只有这样，我们才能迅速地通过法制环 境变化，培养起自己的数字图书馆，或者说这样的企业、事业单位。让他们能够抵挡谷歌有可能带来的垄断，因为垄断最后会造成肯定卖不了好价钱的局面。<br />
<br />
　　张平：中国发展数字图书馆 解决版权授权是首要<br />
<br />
　　（国家数字版权研究基地主任、北京大学法学院教授 《今日观察》特约评论员）<br />
<br />
　　如果真的是涉嫌侵权的话，那么我想权利人维权，应该追求成本最低效益最大的结果。如果我们现在能证明，在中国的服务器上，能够看到谷歌的这种侵 权行为的话，那么你做好公证，你当然就可以在中国起诉它，就当然能够通过和解解决问题，这也是所有市场经济主体，解决法律纠纷的一个最佳的方式，中国其实 在数字图文馆建设方面，已经先行一步了。我们其实比谷歌还要之前，我们99年就开始有各种各样的商业（数字）图书馆，还有一些公益的数字图书馆。但是都没 有发展起来，原因是都没有解决好版权授权。<br />
<br />
　　薛虹：美国政府应担负公民守夜人责任 规范垄断行为<br />
<br />
　　（北京师范大学法学院教授、中国知识产权研究会理事 《今日观察》特邀评论员）<br />
<br />
　　谷歌这个事情可能是因为谷歌自身的市场支配地位，起了很大的作用。如果在一个充分竞争市场，它有强大的竞争对手，它就会主动地和权利人去谈判协 商，制定合理的价格，这个事件已经在全球范围内差不多一年多了，但我们国家最近才有了一些反响，这说明我们权利人还没有意识到，这个著作权保护的国际性的 问题.大量的社会信息资源，由这一个公司来控制和垄断，还是非常危险的。我认为美国政府应该担负起公民守夜人的责任，来规范这种行为，不能允许这种无限制 的市场行为的扩张，最终垄断了人们获取信息的来源。<br />
<br />
　　马光远：中国应建立自己的数字图书馆来对抗垄断<br />
<br />
　　（《今日观察》评论员）<br />
<br />
　　谷歌侵权事件应该是全球最大的一起关于网络知识产权保护的最大风波，它的波及面非常广，全球所有的国家可能都被波及到。从这个事件上来看，大家 最担心的倒不是建立数字图书馆，因为数字图书馆的发展是大家所需要的，大家最担心的其实是垄断。如果谷歌一家独大，那么对全球所有的相关利益人造成的损害 是不可估量的。<br />
<br />
　　我们应对谷歌侵权事件的本身，首先就是要维护目前已经被涉及的一些作家的合法权益，最重要的更长远的应该是建立自己的网上图书馆，与谷歌进行抗 衡。中国的优秀作品和文化应该由中国在线图书馆来弘扬，这是每个人所希望的。所以对抗谷歌垄断最好的手段就是我们争锋相对的建立跟它一样的东西。<br />
<br />
　　刘戈：谷歌应坚持其“不作恶”的企业宗旨<br />
<br />
　　（《今日观察》评论员）<br />
<br />
　　谷歌把不作恶作为自己的企业宗旨，这是因为当年有一位老工程师担心很多搞商业的人进入到谷歌技术公司以后，会因为商业目的最后导致恶性竞争或垄断，所以他就在会议室一个小黑板上写出“不作恶”这样一个词，后来这个词成为企业共同遵守的一个宗旨。<br />
