【北美在线 NAOL.CA/NAOL.US】 2009/01/23 分类：美国华社 来源：多维社<br />
<br />
“看起来是朱海洋控制一切。”当朱海洋带杨馨来看房子时，房东观察出这一点。<br />
1月23日，警方搜索了朱海洋及杨馨的住处。除了计算机、通讯记录、相机，还带走了一本杨馨生前的日记。杨馨为何被杀？相信已离答案不远。<br />
但是，朱海洋为何选择斩首如此重手？根据警方证词，当他被捕时，除了作案用的一把菜刀。在他随身的背包里，还放满了各式锐利物品。他有愤怒？<br />
目前许多中国网民，相信在qq.com的这个博客，是朱海洋真实的个人博客。他在08年9月17日，来美约5周后发表的这篇短文，被归类为个人日记，也是他博客上的最后一篇文章。目前，已有1万多人次阅读。摘录如下：<br />
<br />
“被录取到很不如意的专业，心情糟得很，真是欲进无味，欲退无路啊。我即将毕业，但基层的艰苦，学界的清贫，商界的智斗，政坛的……我都不想去工作了。我很清高，看到许多人趣味低俗，心里很气愤，很孤独。我有很多梦想难于割舍，为此活得很痛苦。能否解脱呢？我很要强，有人说我很虚荣，我心里承认，但又改不了，因为不想让别人小瞧。我一向成绩优秀，名列前茅。但上学期考得很糟，很失败。我害怕失去优势地位，心里压力很大。我对社会现实中的许多事情非常不满，可为什么那么多人在说好话？我自认是悲观主义者，常感悲沉，看到很多笑脸都显浮浅。可是，毕竟听到很多人、看到很多书上都说要“笑对生活”。笑还是不笑？我总是优柔寡断，患得患失，好像什么事都拿不起、放不下，很烦。亲人的期望、自己的信念，都是只许成功不许失败。我感到竞争压力很大，活得很累，几乎每天都很紧张。”<br />
<br />
“父母很关心我，老怕我受苦、吃亏，因此给我安排了很好的生活和前途。我有点不是滋味，但又耽于坐享其成。上大学后，我发现自己默默无闻，有种既高傲又卑微的复杂心态，极想将来出人头地，名声显赫。但现在，看不到什么希望，觉得很没意思，成天在混日子。我很爱面子，希望每个人都对我有好印象，为此活得谨小慎微。但是，仍有人对我不以为然，我很不甘心。在大学里，我虽兢兢业业，但仍很平凡，无论那方面都不突出，心里羡慕那些叱诧风云的同学。对自己，颇感失望。我坚信有错误的人应受到严厉的惩罚与制裁。但有时，现实令我很气愤。为什么不能？”<br />
<br />
“他是个好斗、有敌意的人。”朱海洋的房东说。根据24日的《华盛顿邮报》，房东Will Segar表示，朱海洋似乎很在意金钱。与3位室友分租一间3卧房的公寓，但他坚持不开冷气。甚至在客厅积累木柴，用壁炉取暖。由于水管结冻，引起邻居的抗议。房东装了强制的温度设定器，定在65度。结果朱海洋直接在锅炉切断暖气。“他说暖气太贵了。他在股市赔了很多钱。”房东说。<br />
<br />
08年8月30日，朱海洋写了篇来美三周日志，发表于个人博客。摘录如下：<br />
<br />
“一转眼来美3周了，概括起来就是一个字：“忙”，报到、搬家、orientation、GTA workshop、熟悉校园、上课、作业、买菜、做饭......对个人能力简直就是全面提升。虽然每天一般要忙到12点甚至1点才睡，早上7点甚至6点就要起床，但是过得很充实，完全没有国内研究生期间混日子的负罪感。”<br />
<br />
“美国是个资源浪费大国，我们院系里下班经常灯和空调都不关，浪费；洗手间不用烘干机烘手，用质量很好的纸擦手，浪费；校园里到处是免费拿的资料和手册，翻一翻就扔掉了，浪费；美国人喜欢开很拉风的SUV、SRV和皮卡，也超费油，浪费；超市里物资供应过于丰富，肉便宜绿色蔬菜贵，以至于经常肉买太多吃不掉，扔掉，浪费；等等等等。”<br />
<br />
一个学期后，09年初，他遇到了同校的新生杨馨。根据24日的《华盛顿邮报》，杨馨在1月8日到学校报到后，校方为她安排了一个伙伴，就是朱海洋。接下来的两个礼拜，朱海洋陪着她熟悉校园、找房子，杨馨并将他登记为自己的紧急联络人。<br />
<br />
“所有人都很惊讶。”国际学生主任Kim Beisecker说。印象里，杨馨的个性甜美、心地善良；朱海洋则是有礼貌、乐于助人。<br />
<br />
事件发生后，杨馨的朋友为她在Facebook做了纪念网页，希望她安息。校方与中国学生会的干部们见过面，相信不会有后遗症。“因为在07年4月的枪击事件后，我们的学生已能完全接纳国际学生。”发言人说。<br />
<br />
据报导，华府的中国大使馆23日已与校方接触，安排与朱海洋见面。目前，校方正配合美国国务院，安排受害者的家属来美签证。然而，由于中国春节将至，作业上可能会有延迟。<br />
