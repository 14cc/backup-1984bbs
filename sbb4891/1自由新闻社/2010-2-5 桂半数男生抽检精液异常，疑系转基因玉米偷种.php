<br />
　　也有人注意到，早在2007年，关于转基因作物发展的16字方针已经从“科学规划，积极研究，稳步推进，加强管理”调整成“加快研究，推进应用，规范管理，科学发展”。这个转变没有引起太多人关注。<br />
<br />
　　随着“提速”的开始，只过了大概一年时间，之前被认为存在“危险”的技术，就悄无声息地被批准生产。<br />
<br />
　　相关部门对转基因水稻种植的许可，被认为是国家一个长期工程的必然结果。在2004年公布的《国家中长期科学和技术发展规划纲要（2006~2020）》中，“转基因生物新品种培育”被定为16个重大专项中的一个，总预算超过200亿元人民币。<br />
<br />
　　从那时起，转基因就“变成了非常热的研究领域”。蒋高明评价：“不管发现出了什么问题都不会停，因为钱一开始花，就停不住了。”<br />
<br />
　　此次得到安全证书的项目，据悉自1995年起就开始进行研究，2003年至2004年已经完成了生产性试验。该项目的研究人员曾表示“不希望无限地等下去”，因为一个现在很有优势的品种，若干年后就可能丧失其竞争力。<br />
<br />
　　如今，他的希望已经成真。尽管农业部在回复中强调，该转基因水稻“还要通过品种审定并获得种子生产和经营许可证，方可进入商业化生产应用”，但当消息传出时，与种子生产有关的3家上市公司股票应声暴涨。<br />
<br />
　　农业部评价称：“这是我国转基因技术研究取得的具有自主知识产权的重要成果，为商业化生产打下了良好基础。”<br />
