　　1<br />
<br />
　　这份回复，使普通人开始关注“农业转基因生物安全委员会”这个听上去十分专业的机构。<br />
<br />
　　有很多证据都表明这个委员会的重要性。资料显示，它的设立，根据是《农业转基因生物安全管理条例》，组成人员则是“从事农业转基因生物研究、生产、加工、检验检疫、卫生、环境保护”等方面的专家，每届任期3年。前一次换届，新华社曾专门就此发布消息。<br />
<br />
　　不过，人们很快发现，这个拥有数十名成员的庞大机构，平常人根本难以触及。尽管许多人努力地寻找它的成员身份，网上也充满各种询问和打探，但都没有结果。连这个机构最早成立的时间，外界都很难查到。<br />
<br />
　　中科院植物研究所首席研究员蒋高明认为，作为转基因作物生产安全证书发放前的最终环节，该委员会的作用不容小视，而这个证书则是“转基因作物上市前最难突破的关口”，如果有谁能担保转基因食品不会出现问题，那么“我们请求公布这些专家的名单”。<br />
<br />
　　这个请求无人理睬，蒋觉得这一切“是暗箱操作”，他认为，这说明相关人士对转基因水稻“自己也没底”。<br />
<br />
　　绿色和平组织的工作人员，曾花费很多时间来搜索这个委员会的信息。最终，他们只在中国农业大学的校园网上找到一篇新闻，透露该校的5名研究人员被聘为第三届生物安全委员会委员。<br />
<br />
　　绿色和平组织负责农业项目的官员方立峰觉得，与转基因相关的整个共同体都失声了。这种失声，成为人们的质疑重点。<br />
<br />
　　作为一家民间机构，绿色和平一直强烈反对转基因粮食作物的种植。“我们一直希望，这种关系到人们生命安全的信息应该公开，人们即使不能决策，起码该有知情权。”该组织这样解释其立场。<br />
<br />
　　而与民间组织的活跃相比，事涉其中的政府机构与科研人员，则显得极为低调。<br />
<br />
　　蒋高明是为数不多愿意公开表态的专家之一。他的研究领域与转基因技术无关，是生态农业，这让蒋高明觉得有点尴尬，因为那些更应该表态的人“实在太安静了”。<br />
<br />
　　“由于转基因食品的安全问题至今还存在争论，对转基因水稻的审批起码应该透明。”他通过博客和发表文章数次呼吁。不过，他没有收到任何反馈，无论是赞同的还是反对的。<br />
<br />
　　事实上，在媒体将各种质疑重复了几遍以后，农业部工作人员在电话里表示，“现在各大媒体、部属媒体和地方媒体都已经不再炒作此事了。”<br />
<br />
　　2<br />
<br />
　　人们要求知情权和信息公开的声音背后，是对转基因食品一直存在的巨大忧虑。人们担心，经过转基因的作物，在被人类食用以后，会不会有“无法预料”的后果。<br />
<br />
　　新获得安全证书的“Bt汕优63”转基因水稻，被认为可以杀死昆虫，并且防治虫害。针对这一特性，网上最常见的攻击是这样开始的：既然虫子都不能吃的东西，人能吃吗？<br />
<br />
　　这个质疑已经被专业人员化解。中国水稻研究所一名研究员表示，这种导致昆虫死亡的基因，针对的是某种昆虫特有的基因编码的蛋白质，不会对人造成危害。<br />
<br />
　　为了证明这种作物的无害性，早在获得安全证书以前，参与研究的华中农业大学研究员林拥军就曾表示，对这种转基因水稻，他们先后在小鼠、大鼠等身上进行了毒理试验，并没有发现任何问题。<br />
<br />
　　这种论调，也在网上引起攻击。一种质疑是：“你们能把人当老鼠吗？”<br />
<br />
　　尽管并不掌握完整的科学信息，人们仍然无法消除对于这种人工作物的忧虑。许多人在网上将批准生产转基因水稻视作“科学研究”突破了“道德底线”，并为自己的健康感到担忧。<br />
<br />
　　事实上，关于转基因作物是否存在危险，国内外的科学界一直没有定论。相左的证据不断出现。反对者引述国外科学家的报告称，实验中，幼鼠食用转基因土豆后，内脏免疫系统受到了损害。但支持者的实验结果则与此相反，是“丝毫没有出现损伤”。<br />
<br />
　　保守的国家和地区，比如欧盟，因为科学家无法提供证据证明转基因作物确实无害，因而严格地限制转基因作物的种植和进口。在美国和中国等国家，因为反对者同样无法提供证据证实转基因作物有害，政策一直倾向于推广转基因作物。<br />
<br />
　　之前，番茄、棉花、大豆等转基因作物已经在我国大面积推广，并且一度在防治病虫害等领域取得了辉煌的成绩。但对水稻等主要粮食作物，几乎所有国家都采取了谨慎态度。<br />
<br />
　　中国水稻研究的权威袁隆平表示，他本人的学生也参与了水稻转基因的研究。不过，不久前，袁隆平公开表示，政府应该特别慎重批准转基因植物商业化。他的理由是，科学家不能完全预知对生物进行基因改造，有可能导致何种突变。<br />
<br />
　　“虽然实验非常成熟，但其对人类可能造成的影响，或许要在未来几代人后才显现。”袁隆平说。<br />
<br />
　　在这种情况下，国内一些转基因项目的研究者，曾反复抱怨他们的研究成果市场化被“过于严格”的审批过程阻挠。<br />
<br />
　　对于转基因技术，还有很多别的质疑。许多科学家担心，如果转基因作物的基因通过授粉等途径向四周“漂移”，可能会引发生态危机。另一个质疑指出，在推广转基因棉花时，因为管理混乱，棉花种子来源很杂，许多都没有得到正式的评估和批准，有人甚至说不出自己所种的棉花来源。人们担心，和穿在身上的棉花相比，最终要吃进嘴里的水稻，如果管理不善，可能会引起不可知的危险。<br />
<br />
　　3<br />
<br />
　　2008年，农业部官员曾表示，生物安全委员会每年举行两次例会，会上每次都要讨论“是否批准转基因水稻”的问题，始终无法通过。当时该委员会还是第二届，委员共有74名，有成员在接受采访时表示，转基因技术“出发点是好的，效果是很明显的，但危险是不能预计的”。<br />
<br />
