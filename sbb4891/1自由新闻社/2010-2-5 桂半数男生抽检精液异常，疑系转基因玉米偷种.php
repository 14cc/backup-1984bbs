<br />
张宏良<br />
<br />
近日，许多网站都纷纷登载了广西大面积种植转基因玉米和当地大学生精液异常的消息。关于大学生精液质量异常、生育能力受到影响的报道，来自于广西新闻网的官方网站，是官方权威机构进行的调查和检测，内容无可置疑。而广西大面积种植上千万亩美国转基因玉米的消息，却未见有官方表态，既不肯定也不否定，因而到目前为止难以证实。只是广西自治区副主席陈章良独一无二的特殊身份引起了人们注意，陈章良是八十年代美国培养的生物学博士，被美国《时代》周刊选为全球100位青年人才，回国后搞过国家植物基因研究中心，是以研究病毒基因而著称的一流基因专家。――真不明白为什么不让这位一流基因专家从事基因研究工作，而要派去广西做分管农业的副主席。而另外一位同样留学归来的著名基因专家则被任命为卫生部长。让留学归来的基因专家担任省部级官员，无论是谁的意愿，都有些得不偿失，用林黛玉小姐的话说就是，早知今日，何必当初。既然培养的是省部级官员，当初直接送到中央党校不就得了，干嘛还要不远万里送到美国去培养？<br />
<br />
比之让一流基因专家不搞基因研究而去当官更加不可思议的，就是昨天2月1日举行的两场新闻发布会。昨天上午和下午，由国家农业部和广西自治区政府分别举行了新闻发布会和答记者问。由于种植转基因主粮是农业部决定的，广西又是传言中率先大规模种植美国转基因玉米的省份，所以这两场新闻发布会格外引人注目。知道情况的所有人都认为，国务院是要通过这两场新闻发布会，采用答记者问的方式，就国人所关注的转基因主粮问题，给老百姓一个说法。可结果却是让人大为震惊，震惊得几乎目瞪口呆，简直比看到地球倒转江河倒流还要更加不可思议。专门针对转基因主粮的社会舆论而举行的这两场新闻发布会和答记者问，居然只字没提转基因主粮，不仅主持新闻发布会的官员没有提，而且连所有参加新闻发布会的国内外记者都没有提，包括一直以攻击中国为己任的美国之音的记者，都绝口不提转基因主粮问题，而只提问了两个连央视记者都感到乏味无聊的技术问题。从美国之音记者乏味无聊的提问上，我们看到了什么叫国家利益，看到了美国新闻从业人员是怎样维护美国国家利益的，他们无所不在的爱国举动让我们十分敬佩。同时我们也感受到了中国老百姓的悲愤无奈，想起了2009年那句著名格言――“你们算个屁啊!”两场新闻发布会传达出的基本信息就是：我们工作成就巨大，领导对我们高度认可，我们是人民的救星和希望。<br />
<br />
面对如此结果，让人不仅感慨把老百姓“算个屁”，已经是高抬了老百姓，在他们眼里，老百姓其实连个屁都不算不上，真的不如一个屁。除了偶尔可以用来对着电视镜头表演亲民秀之外，老百姓在其他方面的确是屁不如，不仅不如人屁，甚至不如狗屁。<br />
