2004年8月初,墨尔本的某报总编辑专程来领馆拜会中国驻墨尔本的郑伟章参赞,特地推荐了由马来亚大学(University of Malaya)博士罗斯利-欧玛(Rosli Omar)先生撰写的一份关于转基因工程的报告。该报告是欧玛先生呈交给英国内阁办公室转基因作物小组的。他在报告中指出了基因工程、转基因生物对环境、农业和人类健康带来的种种危害,建议最好禁止所有的转基因作物。一家之言,仅供参考。中国驻外使馆特将将该报告(原文附后)要点翻译撮录如下,希望引起国内有关部门对此问题的重视。　　<br />
<br />
　 　<br />
<br />
长期以来,遗传工程一直被“神化”,宣扬为农业发展的新革命,而对它的负面危害却很少谈及。特别是各国政府的高层决策者很难听到转基因生物有什么危害。长此以往,肯定会对人类的未来发展产生致命的错误。本文重点将阐述转基因对环境、农业、人类健康以及生物多样性所产生的种种危害。　　<br />
<br />
　 　<br />
<br />
1、转基因生物破坏生态系统　　<br />
<br />
生态系统是一个有机的整体,任何部分遭到破坏都会危及到整个系统。例如,一些盐碱、沼泽、雨林以及有寄生虫的地区,以前原本不适合农业种植,由于转基因作物的出现,一些农作物可以耐盐碱、耐高温高湿以及抗病虫等,这些地区都被用来种植农作物,从而使原本生活在这里的生物的栖息地遭到破坏,不得不退出这个系统,造成物种的退化、减少、灭绝,使原有的生态系统遭到破坏。　　<br />
<br />
　 　<br />
<br />
2、转基因物种作为新物种破坏环境　　<br />
<br />
自然界里从来没有过转基因生物。它属于一种新生的外来物种。它与自然生物相比,因其体内有特殊基因,有更强的竞争性。比如,植入抗虫基因的农作物,就会比一般的农作物更能抵抗病虫害的袭击。长此下去,转基因作物就会取代原来的农作物,造成物种灭绝。但这个问题,在转基因生物发展的开始阶段很难发现,可能要历时很多年才会显现出来,但等问题出现的时候,为时就已晚了。　　<br />
<br />
　 　<br />
<br />
历史上就曾有过这样的例子。比如,澳大利亚原本没有兔子,引进兔子以后,其数量翻倍增加,吃光了植物,给生态体系造成了很大的问题。大约一个世纪以前,引进的含羞草在澳大利亚迅速蔓延,大大地挤掉了周围的其他植物。美国引进的食肉鱼、龟头花蛇,吃掉了当地的鱼。1959年,英国把尼罗河河鲈引进到维多利亚湖供人们钓鱼消遣。没想到几十年后,河鲈破坏了当地的生态系统,本地的鱼遭受了灭顶之灾。　　<br />
<br />
　 　<br />
<br />
3、杂交作物对环境产生负面影响　　<br />
<br />
转基因可以通过杂交传给亲缘物种。过去的经验表明,农作物杂交可能对环境产生负面影响。东南亚是大米基因多样性的故乡和中心。加利弗尼亚大学的Norman Ellstrand先生和他的同事认为,世界上最重要的13种粮食作物中有12种与其野生的近缘物种进行了杂交。在加拿大,被用作实验的油菜,分别只具有抗草甘磷、谷氨酸磷和咪唑啉酮中一种的功能,后来发现了同时具备这三种功能的油菜,说明这三种油菜之间产生了杂交。而这种油菜对周围的植物造成了很大的影响。　　<br />
<br />
　 　<br />
<br />
4、转基因生物对非目标生物的污染　　<br />
<br />
转基因生物对非目标生物也会造成不利影响。释放到环境中的抗虫和抗病类转基因植物,除对害虫和病菌致毒外,对环境中的许多有益生物也将产生直接或间接的不利影响,甚至会导致一些有益生物死亡。　　<br />
<br />
　 　<br />
<br />
另外,转基因生物将增加目标害虫的的抵抗性。研究表明,棉铃虫已对转基因抗虫棉产生抗性。转基因抗虫棉对第一、第二代棉铃虫有很好的抵抗作用,但第三代、第四代棉铃虫已对转基因棉产生抗性。专家警告说,如果这种具有转基因抗性的害虫变成具有抵抗性的超级害虫,就需要喷洒更多的农药,而这将会对农田和自然生态环境造成更大的危害。　　<br />
<br />
　 　<br />
<br />
5、转基因生物对其他农作物的影响　　<br />
<br />
世界范围内,经营转基因生物的主要有孟山都(Monsanto)、阿凡迪斯(Aventis)和新根塔(Syngenta)等三家垄断公司。他们已将转基因农作物的种子申请了法律保护,全世界只能向这三家公司购买获得。而且,通过在种子内植入特殊基因,使该种子只能种一槎,第二槎种子很难生长；农民已不能通过原始的方法来获得第二年种植的种子,必须年年向这三家公司去购买。这些公司把转基因作物的使用权牢牢地控制在自己手中。 　　<br />
<br />
　 　<br />
<br />
转基因植物通过花粉进行基因转移,导致非转基因植物受到污染。农场主发现,现在已经很难买到纯净的、未受到转基因污染的有机种子。由于缺少必要的隔离措施,转基因农作物与非转基因农作物相互混杂,给非转基因作物造成巨大损失。　　<br />
<br />
这三家垄断公司非常霸道。明明是转基因作物污染了非转基因作物,可他们还强词夺理,经常起诉种植非转基因作物的农场主侵犯了其专利权。比如,有一家种植非转基因农作物的农场主,就因此而被索赔40万美元。　　<br />
<br />
　 　<br />
<br />
6、转基因生物对人类健康造成的不利影响　　<br />
<br />
转基因生物及其产品作为食品进入市场,对人体将会产生某些毒理作用和过敏反应。在过去这几年当中,曾经发生过食用转基因食品不安全的事件。2002年英国进行了转基因食品DNA的人体残留试验,有7名做过切除大肠组织手术的志愿者,吃了用转基因大豆做的汉堡包之后,在他们小肠肠道的细菌里面检测到了转基因DNA的残留物。转基因食品对人体健康的严重影响,可能还需要经过较长时间才能逐渐表现和监测出来。　　<br />
<br />
　 　<br />
<br />
7、生物多样性的重要性　　<br />
<br />
保持生物多样性是减少遭受疫病的侵袭的影响的重要方式。1864年的爱尔兰土豆枯死病,造成了100多万人死亡,几百万人流离失所,原因就是当地人只种植两个土豆品种,而这两个品种又特别脆弱,一发生意外就无法挽救。再举一例,二十世纪七十年代,印尼、印度的稻田发生了草病毒,非常幸运的是,在6273个品种中发现了有一个品种对这种病毒具有抵抗性,现在该品种被广泛种植。另外,1970年,斯里兰卡、巴西和中美洲地区的咖啡作物爆发了咖啡锈,在咖啡的故乡埃塞俄比亚发现了一种具有抵抗性的品种,从而挽救个整个局势。　　<br />
<br />
　 　<br />
<br />
历史上曾有过六次绝种的情况,最后一次是6千5百万前恐龙和其他无数的物种的死亡。转基因生物的影响,可能会象第六次大灭绝一样,异种交配的物种的基因创造出的新物种可能会产生难以想象的后果。　　<br />
<br />
　 　<br />
<br />
8、公众对转基因生物已经产生怀疑　　<br />
<br />
现在,世界上有许多国家开始反对转基因生物,尤其是欧盟。公众已经对转基因生物产生了怀疑。　　<br />
<br />
　 　<br />
<br />
比如,上个世纪九十年代,英国科学家声称疯牛病不会传染给人类,但最后的事实却恰恰相反,老百姓深受其害。特别是,很多搞科研的部门,是由一些行业的大公司支持赞助的,例如,孟山都给华盛顿大学捐献了6千2百万美元,HOECHST公司(现在的AVENTIS)给哈佛大学捐献了7千万美元,所以他们的研究结果很难保持公正。　　<br />
<br />
　 　<br />
<br />
目前,墨西哥禁止种植转基因玉米；欧盟暂缓进口转基因食品；日本、澳大利亚、新西兰等国家都要求在转基因食品上明确贴上标签,说明转基因成分的含量；英国的许多大超市禁止使用转基因生物作为原料生产食品。标有“不含任何转基因生物”的食品已越来越受到人们的青睐。　　<br />
凡此种种。因为转基因产品具有这些负面影响,人类应对转基因生物持十分慎重的态度,以免产生难以预料的结果。<br />
<br />
<br />
