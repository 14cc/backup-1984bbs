																											<div id="postmessage_417313" class="t_msgfont">习近平向百万名基层党组织书记发送问候短信<br />
 2010-01-06<br />
习近平发给社区书记的问候短信。<br />
<br />
　　星岛环球网消息：全国基层党建工作手机信息系统5日正式开通，习近平向全国100万名基层党组织书记、大学生“村官”发出问候短信。系统收集汇总了全国100万名基层党组织书记、大学生“村官”及省、市、县党委组织部长手机号码，通过手机短信互动，实现了中央组织部、省区市党委组织部与基层党组织书记、大学生“村官”之间快捷、及时、双向、安全的联系沟通，是运用现代化手段加强党的建设的一项新举措。<br />
<br />
　　《钱江晚报》报道，“如果不是其他社区书记都收到这条短信，我真不敢相信，这条慰问短信真的是国家副主席习近平发来的呀！”昨天下午，杭州翠苑街道翠苑三区社区书记孙卫平的内心可谓一波三折，心潮澎湃！从收到短信最初的不相信，到之后的打电话求证，到最后的惊喜，甚至下班回到家都不忘拿出手机打开短信给家人分享。<br />
嘀嘀嘀　国家副主席发来问候短信<br />
<br />
　　原来全国基层党建工作手机信息系统昨天正式开通，中共中央政治局常委、中央书记处书记、国家副主席习近平出席了开通仪式，并通过全国基层党建工作手机信息系统，向全国100万名基层党组织书记、大学生“村官”发出了问候短信。<br />
<br />
　　“短信的落款可是咱们国家副主席啊！太激动了！”孙书记说，一开始自己也不那么笃定，收到短信后马上拨通了翠苑街道保亭社区书记章建萍的电话。“我也收到信息了，正感动呢。短信平台都布到我们基层了，这么高的效率，我还挺自豪的。”听章书记这样说，孙书记还是感觉不真实，就给翠苑街道其他社区的几位社区书记都打了电话。听说大家都收到了，心里才真正踏实下来：“真的，是习副主席发来的！”<br />
<br />
　　这条短信　一定会好好珍藏<br />
<br />
　　昨天，和翠苑三区社区的孙书记一样激动不已的，还有许许多多最基层的社区书记主任、大学生“村官”们。<br />
<br />
　　“我代表党中央，向全国的基层党组织书记、大学生村官致以亲切的问候。”杭州小营街道茅廊巷社区书记顾云一字一句地反复念了好几遍短信。“在寒冷的冬天收到远在北京的国家副主席的慰问短信，真的很温暖。”顾书记收到的这条短信甚至在社区其他社工中一一传阅。“大家的反应和我一样，又惊又喜！”顾书记告诉记者，在收到这条祝福短信的同时，还收到了另外一条短信。“这条短信是关于全国基层党建工作手机信息系统的解释说明。如果我们基层社区书记主任有意见建议，或者需要咨询政策也可以通过直接回复或者发送短信的方法直接传达。太好了！”<br />
<br />
　　“这条短信，我一定会好好珍藏。”宁波服装学院毕业，2008年10月到建德市李家镇沙墩头村当了一名大学生村官的张维，昨天同样收到这条慰问短信，也按捺不住内心的激动，“虽然我们只是一个小小的村官，但是党中央还是非常关心我们啊！”张维说，她一定会在基层好好干，服务农村，服务老百姓，并且把老百姓的心声传递给党中央。</div>

							
							
							
							
