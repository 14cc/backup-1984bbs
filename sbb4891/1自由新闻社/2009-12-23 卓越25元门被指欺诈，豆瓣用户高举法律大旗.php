																											<div id="postmessage_391329" class="t_msgfont">昨日卓越将一批书（如全套的二十四史，价值数千元）错误标价为25元，引起豆瓣用户哄抢，《二十四史》一跃成为卓越销售量最高的书之一。更有用户一次订购数十套，价值数万元。卓越取消订单后，豆瓣用户发帖声讨卓越的欺诈行为，部分用户要求卓越归还他们已经付款的（25元）书籍，另一部分要求对薄公堂。最后卓越道了歉，并将各笔“25元”退换成礼品券......<br />
<br />
<a href="http://www.donews.com/Content/200912/c77a00bd528b425b99a5fedf83d9e41d.shtm" target="_blank">http://www.donews.com/Content/20 ... a5fedf83d9e41d.shtm</a><br />
<br />
12月23日凌晨2点，用户刘先生在卓越亚马逊闲逛，无意间发现全套的二十四史、明史、清史稿、汉书补注、全宋词、新唐书、旧唐书、李太白全集、苏轼文集等平日里标价数百元乃至数千元的数百套古典典籍，卓越价仅要25元，欣喜之下，他一口气连买了九套图书，并选择了网银支付方式，将钱汇到了卓越亚马逊账上。然后，他收到了卓越亚马逊的订单确认函，还觉得不放心，他又去自己账户上查看多次，发现订单都处于正常状态，于是，自以为捡到大便宜的他只等着送货上门。<br />
<br />
　　没想到的是，23日上午，当他在登录自己的卓越账户查询时，却发现，他所下的订单中，二十四史、全唐诗等几本书的订单已经被莫名其妙的取消，而到中午时，包括全宋词在内的几本书的订单也被取消，仅剩下一套《苏轼文集(套装全6册)》 、一套《李太白全集(上中下)》的订单还未被取消。刘先生担心，这两套书也可能随时被取消。他还担心，已通过网银支付给亚马逊的款项，至今还未退还到他手中，这笔款项不知卓越亚马逊将如何处理。<br />
<br />
　　惊魂未定的刘先生致电卓越亚马逊客服，被告知订单被取消是系统故障。客服并建议刘先生重新下单，并承诺赠送刘先生10元礼券作为补偿。但此时，上述图书的标价均已恢复到了数百、数千元，与他当初下订单的标价相比已相去甚远。为此，刘先生认为卓越亚马逊上述行为属价格欺诈，是以看上去离谱的低价诱饵为招徕手段，然后故意取消订单，引导用户去购买未经优惠的商品。<br />
<br />
　　事发当日，类似刘先生这样的投诉在豆瓣等图书社区并非个例。其中有仅购买了一套的，也有购买多套的；有选择货到付款的，也有选择网银支付的；但无一例外的，上述用户的订单均遭到莫名其妙的取消，而且事前卓越亚马逊均未向用户电话或邮件通知，事发之后，也未向用户就此进行解释或道歉。<br />
<br />
　　记者注意到，卓越亚马逊在相关的用户条款中约定，如果用户订购了产品，用户的订单就成为一种购买产品的申请或要约。如果某一商品的正确定价高于系统出错或其他原因导致的错误定价，卓越会酌情决定与用户联系，或者取消订单并通知用户。<br />
<br />
　　卓越的上述做法引发不少当日购买了25元图书的用户不满。在豆瓣社区，不少用户发言认为，卓越单方面取消订单、删改、篡改订单而不提前通知用户的做法，是对用户的不尊重，也有违诚信原则。作为成熟的企业，卓越应该给下订单的客户一定的补偿；<br />
<br />
　　对此，北京市盛峰律师事务所主任于国富律师认为，卓越亚马逊在网上明码标价的行为在合同法上是一种要约邀请，用户下订单是要约，卓越确认订单是承诺，合同自承诺时生效。如果在用户下单后卓越系统自动发出的邮件象邮件里说的那样是“订单确认信”的话，收到此电邮合同即告成立。既然合同已经成立，要解除合同，就应该对方同意之后才可解除，而不能单方面解除合同。如果标价低于正确定价是系统出错，也属于内部问题，错误不在消费者，作为合同相对方的消费者有理由要求卓越方面给一个说法。如果合同订立显失公平，作为当事方的卓越也可以撤销该订单，但应该提前将撤销订单合同的信息告知消费者，否则，消费者有权要求商家继续履行原订单合同。<br />
