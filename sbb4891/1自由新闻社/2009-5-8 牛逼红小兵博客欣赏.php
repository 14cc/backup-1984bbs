2009年4月6日<br />
编译：宋昶志<br />
<br />
菲律宾共产党（CPP）今天热烈祝贺朝鲜民主主义人民共和国（DPRK）在昨天成功发射了一颗通讯人造卫星进入太空。<br />
<br />
与此同时，菲律宾共产党谴责美国政府和它的盟友，“因为它们虚伪的谴责和妖魔化朝鲜的卫星发射是导弹试验而且是对和平的挑衅威胁。”<br />
<br />
菲律宾共产党称，“昨天发射进入太空的通信卫星显示了取得科技进步的朝鲜自卫的毅力和在其社会主义建设中蔑视帝国主义列强因为它的坚定的独立性及反对帝国主义的不懈努力而对它的孤立和惩罚。美国及其他帝国主义列强对朝鲜和它的人民和所有其他斗争着并保持其独立性与锻造他们自己的进步的任何人发泄了彻底的仇恨。”<br />
<br />
菲律宾共产党指出，在过去几年里，朝鲜已经能够实现巨大的技术进步和打破帝国主义在高科技领域的垄断。<br />
<br />
“通过发射通信卫星，朝鲜已经作好准备以打破帝国主义在全球媒体传播的垄断。这不仅将被用作一个平台以打破帝国主义攻击朝鲜的谎言，而且还会促进使用它的其他被压迫民族和人民向世界展示他们的真实情况并宣传他们的要求和打击帝国主义的罪恶。”菲律宾共产党如是说。<br />
<br />
“ 美帝国主义及其盟友更被吓坏的是，该通信卫星的发射同时显示了朝鲜已经证明它有能力发射理论上可以达到阿拉斯加和威胁美国本土的远程火箭。这增加了美国和其他帝国主义列强已经长期抱有的担忧，朝鲜现在已证明了核能力及其打破帝国主义垄断的先进武器。”菲律宾共产党进一步说。<br />
<br />
“帝国主义谴责朝鲜的卫星发射是导弹发射和挑衅美国，日本和其他国家显示了它们的虚伪性，因为它们极力试图保持其使用先进武器威胁和征服其他国家的垄断地位。”<br />
<br />
“美帝国主义正破坏朝鲜已经能实现的和平发展的经济和国家防卫。他们将不惜一切代价，直到朝鲜被强迫打倒屈服”，菲律宾共产党说。“但朝鲜已经表明它不会屈服，面对美国和其他帝国主义列强的威胁，为了社会主义的发展将继续推进其自主的计划。”</div>

							
							
							
							
