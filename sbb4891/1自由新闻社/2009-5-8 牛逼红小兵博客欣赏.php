&nbsp; &nbsp;&nbsp;&nbsp;这不刚说完又错了。吾师说“纳粹、北欧、中国、越南、古巴、朝鲜这些都是‘社会主义’，那大家比较一下它们又有多大的不同呢？”这就太混淆概念了，怎么可以仅因为名称上加个“社会主义”就当做真正的社会主义国家来讨论呢？要是全世界都乱起名字，那世界岂不乱套了？假若共产主义（社会主义的高级阶段）=民主社会主义=法西斯主义=社会主义，那世界上的名词真的要进入相对主义时代了。<br />
