&nbsp; &nbsp;&nbsp;&nbsp;我想起高中时在多堂课上与多个老师发生过的对辩，可我在公众场合是表现内敛的人，除非公众都是认识的人，不然是多作壁上观而已，我更倾向于静观其变、后发制人。不过想起了大汉奸马立诚先生半年前曾透露的一次窘态，他某次在深圳参与活动时，一个中年男子突然站起来驳斥其谬论并声言怀念毛主席的时代。我想，在大众觉醒的今天、在阶级斗争和意识形态搏斗异常激烈的当下、在左派急需张广其影响力的新时代，如果我们继续选择沉默，无异于选择沦亡。要是全国的左翼进步群众和爱国青年学生能面对着上至像曾担任《人民日报》主编的大学者、大汉奸马立诚那样的权威，下至在天下一隅育人树人的普通学校的持有错误观点的教师都能挺身而出，那么，谣言、污蔑、中伤还能安然传播吗？<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;我的喉咙噪得很，干脆一吐为快地咳它个够劲。连续且洪亮的咳嗽声在放完电影的黑乎乎的课室里响起。吾师问曰“怎么回事了？”我当即站起来陈言：老师，我对你的话有不同看法，你刚才说的什么“3000万”、“7000万”都是海外民运分子和右派捏造出来的谣言。你可以想象，在那个年代，要是死了几千万人，平均下来理应是每家每户都有人或亲戚死亡，但你问问在座的各位同学，他们有没有家人或亲戚在三年困难时死亡呢？所以，我认为你刚才的话有误导学生之嫌。”言毕坐下，满场百余坐位已是掌声一片。吾师表现出一般师表应有的仪态，说“我觉得这位同学能站起来说出自己的观点，有自己的独立思考，很好。但是……”接着，吾师先以自己家乡（甘肃东部）为例，再引右经据右典地表面自己的话是“事实”。为了让多数同学先上完课，我没有当场继续阻击。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;临近下课，吾师又补充道：“当然，在课堂上大家都是讨论……我认为我的看法应该比较有道理，主要有两点，一是我年纪比你们大，经历过的事情也多一些。二是我的学历高一些，学过的东西多一些。大家回去可以看看我写在黑板这几本书，都是有关大饥荒饿死人的历史的……历史事实是不会改变的，真相总会披露”下课后，我信步走上讲台开始庖丁解牛地分解我不认同的观点。我说“老师，你所列出的这些作家都是持右派观点的，当然不可能得出客观的结论，这个杨继绳就是专门在反动刊物《炎黄春秋》上撰文的家伙，这个写《定西孤儿》的杨显惠也是那类人。而您以您的家乡为例就有点以偏概全了，你们那里都接近定西了，当然是重灾区，其他地方的情况并不相同。”<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;“老师，您列的这些书和作者，这个、这个、这个……我都了解过，但你有没有了解过左派的观点和看过左派写的书呢？例如张宏志的《还清白于毛泽东》，您说那批右派作家写的内容是‘有依有据’，但左派的反驳同样有依有据，那大家都是引用史料，但得出的结果不同，你又怎么可以肯定你说的那些作家写的就一定是‘事实’，而另外一些作家写的就不是呢？所以您在课堂上最多只能说‘这些作家有这样的观点，我也认同’，而不能先入为主地说他们写的就是‘事实’，我建议您可以看看一个叫‘乌有之乡’的网站，里面就有很多反驳这些谣言的文章”。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;我前面已论述过我作为知道分子的战略性有利地位，就是起码不会在争论时发生我“吃的饭还比不上长辈吃的盐多”而显得作为晚辈的我看似先天逊色于人的景象，在没有自我标榜或任何炫耀心态的情况下，我可以实话实说地透露，在人世活到今天的我还不曾遇到过任何所知的东西比我多的知道分子。当论辩时涉及的知识面越多，往往是越有利于我们知道分子，尤其是我这个“泛知道分子”的。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;吾师问“你知道了，但你有没有看过呢？”我说“我只是了解过，但我在这里强调的是，我对各方的观点都有所知悉，起码比较过各方的观点，所以我估计我知道的可能比您多一点，综合分析得全面一点。您说的那些作家和书籍，我都知其一二了，但我说的那些作家和书籍，老师您还没有看过。因为现在右派的书还可以公开出版，而左派的书很难出版，这还是一个话语权的问题。例如法新社、美国之音等西方媒体都一直宣扬中国三年困难时期死了3000万人的观点，但它们的话有多少可信度呢？我相信您也知道美国之音是擅长造谣的。但现在世界上还有很多人相信他们的各种谣言啊，这就说明历史是可以被撰改的，好比多数人听了孟姜女的故事都认为秦朝发生过孟姜女这么一个女子的惨剧，但事实是孟姜女根本就不是秦朝人。”<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;我接着说“归根结底还是阶级的问题，站在不同阶级立场的人就会有不同的看法并宣传利于其阶级的观点”。吾师反问“什么阶级，美国等西方国家都是言论自由的社会，在言论自由的社会大家都可以说话，你说你的、我说我的，其他人也可以自己甄别。”我直言道：“美国实行资产阶级专政，在资产阶级专政的国家，是资产阶级媒体掌握了宣传权力，在他们占有优势的情况下，怎么可能有真正的言论平等呢？再说，在无产阶级专政下也有言论啊，文革时期就有四大民主：大鸣、大放、大字报、大辩论。”吾师回应“你说的阶级的观点都是正统的说法”。我也复曰：“勿论其正不正统，我说的只是马克思主义的阶级分析法，我还看不出有任何人能驳倒其理论。阶级地位不同导致阶级利益冲突是大家都显而易见的事实，这就是科学，我相信的是科学。”<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;吾师拿出纸张，边写边说，你看看这几个人的书吧，我见其写下“林达”，便说“哦，不就是一个留美的吹捧美国制度的右派学者嘛”。大概他认为我知道了就不必写了，他又反过另一面写“哈耶……”，还没待他写完，我就说“哈耶克就是一个带有偏见的资产阶级学者，你想写他的《通过奴役之路》吗？”吾师见我也知道，便说“你知道就行了”，他又写下一行“普波……”，我插话道“波普尔的《开放的社会及其敌人》吧？”，他一听也忙说“是、是”，并把“普波”两字改为“波普”了。走下教学楼途中，我又继续交流了几句，最后，吾师问：“你是哪里人？”、“你是城镇的还是农村的？”我想，青年时就当“泛知道分子”还是不错了，假如恰遇其他知道分子，兴许能收获凭本事得到的尊重。顺便说一句，我相信“泛知道分子”确实更能辨析是非，因为当一个人接触过各方的观点和人类文明浩瀚无边的大量信息或知识后，他们会看穿世界的多面性，他们更能保有怀疑主义立场及多做全面分析。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;其实，我是相信吾师在传播所谓“3000万”的谣言时是真心相信的，我认为他被误导了、被欺骗了，他并非有意为之，所以我并不过多抱怨他。但仍然要清清楚楚明辨的是，这么庞大的历史事件统计，怎么可能由几个右派混账学者凭自己去几个地方收集资料并做做加减乘除的算数就轻易得出真实数据呢？所以，即使是从情理逻辑来判断，所谓“三年自然灾害饿死3000万”都是绝无丝毫信服力的弥天大谎。我希望有一天，任何有文化、有道德、有思想的人，特别是知识分子都不要再传播谣言，尤其是这些阴险恶毒且虚假到用常人思维都能判断其虚假的无耻谰言。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;回到宿舍，舍友问我怎么比往日晚回来，听我说完来龙去脉后，舍友忙说“根本就不可能，一是用常识就能分析出不可能死了几千万人，二是我以前也问过我的家人，我们那里也算广东欠发达地区了，三年困难时期也没有死人，即使是文革也没有人被批斗死。”我开怀地付之一笑，是的，历史总是历史，事实也总是事实，但阐述历史和发掘事实并非资产阶级造谣家的专有权，谁也妄想通过哗众取宠的耸听危言捏造所谓的“真相还原”、“历史事实”。诚然，任何时候我们都需要探究、披露历史，但我们更需要揭露拿笔杆子杀人的垄断历史传播的右派的欺世丑行。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;吾尊重吾师，吾更尊重真理！<br />
