&nbsp; &nbsp;&nbsp;&nbsp;晚上上选修课，幸好我选的是“电影鉴赏”，就当每个星期看场电影吧。上周看的是阿伦雷奈的《夜与雾》，基本是纳粹集中营的历史镜头。看着累累白骨，连心理承受能力良好的我都定睛而视了。今晚看的是纳粹党拍摄的纪录片《德意志的胜利》，瓦格纳艺术搭配下的法西斯美学确实有吸魂的功效，但我清醒得很。<br />
