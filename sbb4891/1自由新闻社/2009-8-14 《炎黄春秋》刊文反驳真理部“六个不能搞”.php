													<h2>《炎黄春秋》刊文反驳真理部“六个不能搞”</h2>
						
						
																											<div id="postmessage_202685" class="t_msgfont">这篇文章把真理部“六个不能搞”逐个反驳了一遍，请大家深入学习与领会，武装自由头脑，此文可作为反驳五毛的优质材料，并强烈建议推荐给那些刚刚对中国恶政感兴趣的朋友。<br />
<br />
先看一下什么是“六个不能搞”：<br />
<br />
为什么要坚持马克思主义在意识形态领域的指导地位而不能搞指导思想的多元化<br />
为什么要坚持中国特色社会主义而不能搞资本主义<br />
为什么要坚持公有制为主体、多种所有制经济共同发展的基本经济制度而不能搞私有化或“纯而又纯”的公有制<br />
为什么要坚持人民代表大会制度而不能搞“三权分立”<br />
为什么要坚持中国共产党领导的多党合作和政治协商制度而不能搞西方的多党制<br />
为什么要坚持改革开放不动摇而不能走回头路等重大问题，更好地统一思想、凝聚力量。<br />
<br />
――――――――――――――――――――――――――――――――――――――――――――――<br />
<br />
妨碍政体改革的认识误区 <br />
韩云川<br />
 <br />
2009年第8期 炎黄春秋杂志<br />
<br />
改革开放三十余年来，我国经济体制改革取得重大进展，但政治体制改革存在着严重滞后的情况。原来想通过政治体制改革来解决的许多问题都没有得到真正的解决：权力过分集中的问题、权力制约的问题没有得到真正解决；人民群众当家作主的权利缺乏制度上的保障，侵犯公民合法权益的情况还相当普遍；权力在资源配置中仍然起着非常重要的作用，在一定程度上扭曲了市场在资源配置中的基础性作用；公职人员的腐败现象不断滋生蔓延；等等。这种政治体制与我国社会主义市场经济体制改革不相适应，与人民群众当家作主的愿望和要求不相适应，与世界民主进步的潮流不相适应。<br />
　　为什么会出现这种情况，原因是多方面的。既有我国专制主义文化传统的影响，又有既得利益集团的阻碍。除此以外，认识上的误区也是我国政治体制改革迈不开步子的一个重要原因。这些认识上的误区主要有以下五种。<br />
<br />
<strong>第一种：体制优越论</strong><br />
<br />
　　体制优越论者认为，我们的政治体制是好的，具有强大的生命力和优越性。因此，在谈政治体制改革问题时总是强调坚持和完善我们的各项制度，而不是改革它。这种观点看不到我国政治体制存在的根本缺陷和弊端。这是妨碍我国政治体制改革最主要的认识上的障碍。既然我国的政治体制具有很多优越性和优势，那还改它干什么？我们提出政治体制改革不是无病呻吟么！？<br />
　　其实，苏联模式的政治体制存在着致命的缺陷。1981年6月27日，中国共产党十一届六中全会通过的《关于建国以来党的若干历史问题的决议》指出：国际共产主义运动史上由于没有正确解决领袖和党的关系问题而出现过的一些严重偏差，对我们党也产生了消极的影响。这主要是从领袖和政党的关系这个角度来总结文化大革命的教训的。如果从更深层次看问题，我们就会看到，在苏联的社会主义实践中，存在着无产阶级夺取政权后背离人类政治文明发展大道的严重偏差。这种偏差主要表现在三个方面。<br />
　　一是把无产阶级专政推向极端。<br />
　　在马克思那里，无产阶级专政是有特定含义的。无产阶级专政是人类民主政治的进一步发展，是最大多数人的统治。而十月革命后，在布尔什维克的政治实践中，将无产阶级专政推向极端，实行红色恐怖，取消了俄国人民二月革命后争得的保障人民群众民主权利的一系列自由，包括言论、出版、游行、示威、集会、结社等自由。1918年，德国社会民主党的左派领袖、德国共产党的创始人之一罗莎・卢森堡就对列宁为代表的布尔什维克将无产阶级专政推向极端的做法提出了严厉的批评。她说：自由受到了限制，国家的公共生活就是枯燥的，贫乏的，公式化的，没有成效的，这正是因为它通过取消民主而堵塞了一切精神财富和进步的生动活泼的泉源。没有民主就没有社会主义。十月革命没有使俄国走上民主宪政的道路。从人类政治文明发展的大道来看，这是人类政治文明发展的中断。苏联后来垮台的根子也在这里。现在的问题是我们并没有从苏东剧变中正确地吸取经验教训。<br />
　　过去，我们总是强调资本主义民主是虚伪的、骗人的民主，对富人是天堂，对穷人则是陷阱和骗局。社会主义民主比资本主义民主民主百万倍。似乎社会主义制度一建立，确切些说，似乎只要我们宣布自己搞社会主义（我们所理解的社会主义），就一定是民主制度，而且这种民主制度就一定比资本主义民主民主百万倍。这是一种极大的误解，确切些说是误导。苏联模式的社会主义实践证明，真正的民主制度在这些国家并没有建立起来。这些国家实际上是绕开了民主革命的任务。从建设民主政治大厦这个角度看，这些国家存在着严重的偷工减料的问题。由于这些国家并没有完成民主主义的任务，因此，在苏联模式的社会主义国家里，政治清洗和镇压就如影随形。比如，上世纪30年代的苏联大清洗，40年代对所谓铁托分子的清洗，50年代的波兰、匈牙利事件，60年代的捷克斯洛伐克事件以及中国的文化大革命，等等。由于这些被称为社会主义国家的政治大厦中缺乏民主的支柱，最后都轰然倒塌了。这都是偏离人类文明发展大道的结果。苏东剧变证明了一条真理：一切不民主的制度都会死亡。<br />
　　二是将共产党的先进性置于无须检验的地位。<br />
　　在《共产党宣言》中，马克思和恩格斯就清楚地告诉我们，无产阶级政党并不就只有共产党一个，而是有多个。共产党只是众多工人政党中的一个政党。这说明，当无产阶级取得政权后，就有一个共产党与其他工人政党的关系问题。<br />
　　而俄国在十月革命后，其他的政党先后被布尔什维克党所消灭。<br />
　　那么，领袖、政党、阶级、群众之间的相互关系是怎样的呢？列宁当时是这样论述的：“群众是划分为阶级的；……在通常情况下，在多数场合，至少在现代的文明国家内，阶级是由政党来领导的；政党通常是由最有威信、最有影响、最有经验、被选出担任最重要职务而称为领袖的人们所组成的比较稳定的集团来主持的。”（《列宁全集》第39卷，人民出版社1986年10月第2版，第21页。）从民主政治的角度看，这段话并没有全面、完整地阐明这四者之间的关系。如果全面、完整地阐明这四者之间的关系，就应该是这样的：群众是划分为阶级的；在通常情况下，阶级是由政党来领导的；政党通常是由最有威信、最有影响、最有经验、被选出担任最重要职务而称为领袖的人们所组成的比较稳定的集团来主持的；究竟由哪个政党执掌政权是由人民群众来决定的。关键是这最后一句。这最后一句话可以说是民主政治的真谛。<br />
　　列宁说：党是无产阶级阶级组织的最高形式。斯大林在他的党的布尔什维克化十二条的第一条中就对列宁的这个公式作了如下的解释：必须使得党不把自己看成为国会选举机构的附属物――像社会民主党在实际上把自己看成的那样；也不能看成为职工会的不要钱的附加品――如同某些无政府主义工团主义分子有时对于这点所反复说的那样；而应看作是无产阶级的阶级联合之最高形式，负责领导无产阶级的其他一切形式的组织――自职工会到国会党团――的使命。也就是说，党不只是一个有组织的部队，而且是超出于工人阶级其他一切组织的最高组织形式。因此，党领导着政府，党领导着军队，党领导着工会，党领导着合作社，党领导着工矿企业、学校，党领导着妇女群众组织和青年群众组织，等等。<br />
　　一个政党是否代表先进，是要接受实践检验的。由谁来检验呢？当然只能由人民群众来检验。通过什么途径来检验？在战争年代，是通过战争的实践来检验，即通过人民群众支持战争的一方来检验。也就是说，人民群众往往支持那些相对来说进步一些的势力去反对那些相对来说保守或反动一些的势力。那么在和平年代如何检验呢？那就要通过民主的方式来检验，在民主政治已成为世界进步潮流的现代社会，通过民主选举的方式来检验。在民主政治体制下，任何政党，是否执掌政权，要由人民群众来选择、来决定。换句话说，要由人民群众来授权，因为人民群众是国家和社会的主人。如果不由人民群众来选择、来决定，那就根本谈不上民主政治。<br />
　　三是实行严格的集中制。<br />
　　马克思和恩格斯都没有明确使用民主集中制的提法。第一个明确地提出“民主集中制”的是列宁。民主集中制的提法并不是强调民主，而是侧重强调集中。实行集中制是由俄国当时特殊的历史条件所决定的。在沙皇统治之下，俄国没有民主可言，当时成立的革命组织基本上都是秘密的，这些组织都强调集中制。俄国社会民主工党建立之初，由于组织松散软弱和处于秘密状态的特殊条件，列宁强调党要实行严格的集中制。1902年，有社会民主党人给列宁写信质疑集中制，认为集中制容易造成个人专断，会导致很严重的后果。列宁在复信中说：如果中央偶然出现一个大权在握的无能的人，那么严格的集中制就很容易断送整个事业。这种情况当然可能发生，但是不能拿选举制和分散制作为防止出现这种情况的手段，在专制制度下进行革命工作，无论在多大的范围内搞选举制和分散制，都是绝对不能容许的，并且简直是有害的。<br />
　　如果说，在专制制度下，强调集中制是必要的话，那么在无产阶级夺取政权后，就应该着重建设民主制。但十月革命后，布尔什维克党并没有这么做。而是将民主集中制进一步发展成为国家机关和人民团体的组织原则，并推广到其它国家。<br />
　　民主集中制与民主制有区别。在民主制下，民主具有最高的权威，民主的核心就是少数服从多数，或者说，多数决定，保护少数。民主的结果就自然而然地集中了。而在民主集中制下，集中具有最高的权威。民主的结果并不一定就自然而然地集中了。如果大家发表的意见与集中指导者（领导者）的意见相左，集中指导者会说，下次再讨论吧。如果民主的结果是自然而然地集中了，那还要集中制干什么？之所以要加“集中”二字，就是因为集中高于民主。<br />
　　总之，世界社会主义运动中以布尔什维克为代表的政治体制，曲解了马克思主义。这种政治体制从一开始就存在重大缺陷，就好像一辆设计有严重问题的汽车，如果不改变这种设计，不管驾驶者的技术如何，迟早是要出事的。这种政治体制及其设计思想至今仍然控制着不少人的头脑和手脚。<br />
<br />
<strong>第二种：发展落后论</strong><br />
<br />
　　发展落后论者认为，我国现阶段经济文化发展还比较落后，主要任务是发展社会生产力，提高人民群众的物质文化生活水平。现在谈民主政治建设为时尚早。只有等到经济文化发展到一定的高度，我们才能建立健全的民主政治体制。这种观点完全将政治体制改革与经济文化发展对立起来，似乎政治体制改革一定会妨碍经济文化发展。这种担心是多余的。政治体制改革与经济文化发展是相辅相成的。健全的民主政治体制更能调动各方面的积极性，更能促进社会的经济文化发展。如果说过去我们的经济文化条件落后，进行政治体制改革的条件还不成熟的话，那么，经过三十年的改革开放，我们的经济文化水平已经有了一个相当的提升，全面进行政治体制改革的条件已经具备。<br />
　　1986年9月28日，中国共产党第十二届六中全会通过的《中共中央关于社会主义精神文明建设指导方针的决议》明确指出：我国社会主义现代化建设的总体布局是：以经济建设为中心，坚定不移地进行经济体制改革，坚定不移地进行政治体制改革，坚定不移地加强精神文明建设，并且使这几个方面互相配合，互相促进。我们应该回到这个认识上来。<br />
<br />
<strong>第三种：文化缺失论</strong><br />
<br />
　　文化缺失论者认为，中国的传统文化就是专制主义文化。中国没有民主的传统。为什么中国人能够忍受极权政治？因为中国人几千年都是生活在专制统治之下。专制统治者对人的残酷迫害使人们不敢表示反对意见。还有人甚至说，中国人根本就缺少民主、自由的基因。西方人是“不自由，毋宁死”。中国人是“好死不如赖活着”。因此，中国人已经习惯于生活在极权政治体制之下，没有必要进行民主政治改革。<br />
　　这种观点实际上是一种屈服于专制主义文化传统的无所作为的观点。的确，中国没有民主的传统，专制主义文化根深蒂固。但这并不等于说，中国人民就不追求民主和自由。近代以来，中国多少志士仁人为了追求自由和民主而不惜牺牲自己的生命。人们的生活质量并不仅仅体现在物质生活上，还体现在政治生活和精神生活上。自由民主的生活是人们生活的一种新的境界。当然，中国的专制主义文化传统使中国走上民主政治的道路非常艰难，但这并不等于说，中国就永远不能走上民主政治的道路。只要我们认真吸取人类先进的政治文明成果，弥补中国传统文化的缺失，我们也能建立健全的民主政治体制。<br />
<br />
<strong>第四种：民主动乱论</strong><br />
<br />
　　民主动乱论者认为，中国如果真正实行民主制度，就一定会出现动乱的局面。<br />
　　这种观点是不正确的。只有在健全的民主制度下，社会才能真正的稳定。在健全的民主制度下，社会矛盾容易得到解决，社会生活会秩序井然。相反，在不民主的制度下，社会矛盾会不断激化，只有靠强力维持，社会各方面的关系都很紧张，社会表面的稳定潜伏着严重的危机。我们可以举罗马尼亚的例子来说明这个问题。在东欧国家中，罗马尼亚可以说是一个不搞改革的典型例子，但结果怎么样呢？在罗马尼亚共产党第14次代表大会期间，代表们为齐奥塞斯库欢呼不下60次，但就在14大以后没多久，形势急转直下，齐奥塞斯库夫妇被处死。为什么呢？因为在罗马尼亚，个人专断，政治生活极不正常。在高压的气氛下，人民群众不能表达自己的真实想法。表面上的稳定掩盖着事实上的不稳定。罗马尼亚垮起来可谓是一夜之间。因此我们不能被表面现象所迷惑。现在世界上最稳定的国家不是专制主义的国家，而是民主制度健全的国家，这是不争的事实。如果我们的体制真正能够做到人民当家作主，基层和人民群众的创造性就能充分发挥，社会的各种矛盾就能得到较好的解决，社会就能建立在真正稳定的基础之上。因此，为了国家的长治久安，我们应该致力于建立健全的民主政治体制。<br />
<br />
<strong>第五种：民主分裂论</strong><br />
<br />
　　民主分裂论者认为，中国是由56个民族组成的多民族国家，如果真正实行民主制度，就会导致国家的分裂。<br />
　　这种观点低估了中华民族的向心力。我们可以回顾一下近代的历史。中国在列强瓜分的情况下，都没有出现分裂的局面。辛亥革命时各省宣布独立也并不是分裂，而是独立于清廷，实际上是同专制主义作斗争。即使在军阀混战的年代，中国都没有分裂。维护中华民族的团结和统一可以说是各族人民的共同愿望。在民主政治体制下，更有利于维护国家的统一。<br />
　　民主在本质上是自治的。人民群众是国家和社会的主人。自己的事情自己作主。各个地方的事情应该让各个地方的人民自主地决定。中国共产党第十三次全国代表大会指出：凡是适宜于下面办的事情，都应由下面决定和执行，这是一个总的原则。我们应该坚持这条原则。我们可以设想一下，如果每个地方的事情都由那个地方的人民自己作主，将会是一个什么情况。比如，村里事情，由村民作主，乡镇不干预；乡镇的事情由乡镇居民作主，县里不干预；县市的事情由县市人民作主，省区不干预；省区的事情由省区的人民作主，中央不干预。只有跨区域的事情才由上一级政府来协调或决定。这样事情会办得更好些。各地实行高度的自治，国家只保持五个方面的统一就行了。这五个统一是：统一外交、统一国防（武装力量）、统一海关、统一法律、统一货币。这五个统一不仅适用于内地，也适用于台、港、澳地区。全国的事情由全国人民共同决定，地方的事情由地方的人民自己决定。在民主的基础上更有利于中国各民族的团结和统一。<br />
　　不仅中国大陆的统一需要健全的民主制度，而且完成大陆和台湾的统一更需要健全的民主制度。中国的统一大业还没有完成，大陆和台湾还处在分裂分治的状态。实现大陆和台湾的统一是海峡两岸人民的共同愿望。但海峡两岸如何才能统一呢？我认为，两岸的统一只能走民主统一之路。两岸统一不可能统一于哪个政党，而只能统一于民主。现在，台湾居民的民主意识越来越强，台湾的民主政治步子也越来越坚实。在这种情况下，要实现两岸的统一，只能统一于民主。统一于民主就是统一于进步。其实，海峡两岸都存在统一的意愿，只不过统一的理念不一样罢了。我们有必要树立共同的民主政治理念。只有统一于健全的民主制度，才符合两岸人民群众的根本利益，才符合历史前进的方向。可以这样说，中国民主政治健全之日，就是中国统一之时。<br />
<br />
<strong>（作者系中共中央党校社会发展研究所教授）</strong></div>

							
							
							
							
