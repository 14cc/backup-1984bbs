内容简介： <br />
近代社会客观地本能地同时存在两种需要、两种发展趋向，即“激进”与“改良”，它们是交织在一起，是可以统一的。近代的中国是一个转型中的社会，古代的东西与近代的东西杂混蜕变，本来就不好清理。思想文化范畴的理念与政治范畴的理念并用于救国斗争，越发显得头绪多多。用“激进”与“改良”来评价中国近代史上的政治、思想派别或人物，情况就大为复杂了。 <br />
梁启超在改良与革命之间反复，已经隐约感悟到上述的两种需要；但他更偏重于改良，对两种历史需要的理解有失偏颇。民初他主张在共和制下实行开明专制，无意间，已经找到了中国近代史两种需要的统一方式：先革命，在先进国体的外壳下，进行改良，实行符合社会历史条件的制度。在改良与革命的两难之间，梁启超已隐约参悟了中国近代史的发展规律。 <br />
雷颐指出，“辛亥革命”这些年来几乎成为“激进”的代名词，其实，被尊为“辛亥之父”的孙中山并非一开始就想“干革命”的，起初也是想方设法、最后通过王韬上书李鸿章，想走“改良”路线的。只是在“改良”被拒之后，他才立志走上“革命”一途的。 <br />
有人对清政府的“新政”“立宪”被辛亥革命“打断”而深感遗撼。不经革命的大动荡大破坏而收革命之实效，当然是值得追求的理想状态，作为一种良好的愿望，更是无可指责。 <br />
雷颐认为，几乎每个时代、每个社会都会有各种各样的“激进”思想，但在承平年代开明社会，形形色色的激进思想于民众之影响者几希，更掀不起能动荡社会的大风大浪，只能是屈居一隅的少数人信念，不足为意。然而，<strong>一旦社会腐朽不堪，而统治者又拒绝改革以疏不满以缓危机，民众忍无可忍最后抱定与汝偕亡之心，则蛰伏已久的激进思想便如狂飙突起，成为席卷一切的巨浪大潮。此时此刻，惟最激进者最有吸引力，暴力肯定不断升级，愈演愈烈，最终火焱昆岗，玉石俱焚。 </strong><br />
嘉宾简介： <br />
男，祖籍湖南长沙，1956年出生于湖北武汉，中学毕业后下乡数年，然后当兵，复员当工人。1978年考入吉林大学历史系，1982年毕业，获历史学学士学位，同年考入吉林大学研究生院历史系中国近代史专业， 1985 毕业， 获历史学硕士学位。1985年到中国社会科学院近代史研究所工作至今，现任研究员。 <br />
雷颐先生对中国历史有着深刻的理解，观点独到、分析缜密，研究方向为中国近代思想史、中国近代知识分子与当代中国史。 <br />
