黄云在国会作证时用的是匿名，当他接受全国性电视台采访时，拍摄的画面是他的背影，声音是经过修饰的。出于对中国当局的恐惧，这位轻声细语的硅谷软件咨询师一直隐瞒着他的身份。直到今天。<br />
