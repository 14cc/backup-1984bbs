<br />
By Mike Swift<br />
<a href="mailto:mswift@mercurynews.com">mswift@mercurynews.com</a><br />
Posted: 03/26/2010 07:31:03 PM PDT<br />
Updated: 03/27/2010 06:26:10 AM PDT<br />
<br />
<br />
To Chinese Internet censors, he's infamous. To the rest of the world, he's unknown.<br />
As he clicked through the Web in a Silicon Valley coffee shop Thursday, you'd never guess that a half-million people in China, Iran and other countries depend on his software to evade Internet blocking and government surveillance, that an estimated 50,000 government software engineers in China are trying to stop him and others; and that a congressional panel debated not only how to help mighty Google in its confrontation with Chinese censorship this week, but also the work of this software engineer.<br />
He's testified before a congressional panel ― anonymously ― and when he was interviewed on national television, he was shot from behind and his voice disguised. For fear of the Chinese government, the soft-spoken Silicon Valley software consultant has kept his identity concealed. Until now.<br />
&quot;I realized that if you're scared,&quot; Alan Huang told the Mercury News in an exclusive interview, &quot;the government can take advantage of that.&quot;<br />
Huang's local company, UltraReach Internet, is among a group of companies that make up the Global Internet Freedom Consortium. Through the consortium's simple software, often downloaded through an e-mail, a person can step outside whatever blocking or surveillance their country imposes and freely access anyplace on the Web.<br />
A follower of the government-banned spiritual group Falun Gong, Huang helped develop the technology in 2002 to help members of that movement communicate. But he soon realized that access to unfiltered information, free of government surveillance, was a fundamental need, not tied to any single group or country. Huang was active in the democracy movement in China in 1989 before moving to the Bay Area in 1992.<br />
While the largest share of the consortium's traffic still comes from China, the service is seeing a surge from Iran ― where the government cracked down last year on democracy activists using YouTube, Facebook and other social networking tools to communicate ― and from Vietnam. The consortium also gets many users from Saudi Arabia, the United Arab Emirates and other countries ― including the United States.<br />
&quot;If you don't have privacy and security, you don't have freedom,&quot; Huang said.<br />
Huang is cheering Google's step this week of directing its Chinese search traffic to an unfiltered search service based in Hong Kong. He had, however, already decided that he no longer wanted to remain anonymous when a reporter tracked him down this week, saying the consortium's circumvention software represents &quot;the right side of technology, the right side of history.&quot;<br />
The consortium is one of many services that have become increasingly important tools for people within China to circumvent the &quot;Great Firewall&quot; over the past five years, said Xiao Qiang, director of the China Internet Project at the UC Berkeley and founder of the China Digital Times. He says the demand will only become greater.<br />
&quot;We haven't even seen the full retaliation from the Chinese government&quot; to Google's move to stop censoring its Chinese search, Xiao said. &quot;If Google is forced to withdraw from China, it will make this an even bigger market.&quot;<br />
The global attention Google generated when it stopped censoring search in China could also help the consortium make its services available to more people in all 180 countries it serves. On Wednesday, at a hearing in Washington before the Congressional-Executive Commission on China about Google, a debate over additional federal resources for the consortium and others offering circumvention technology became a central issue.<br />
Mark Palmer, a former U.S. ambassador to Hungary and now of Freedom House, a human rights group, blasted the U.S. State Department for not distributing $30 million in already appropriated money to help the consortium buy more computer servers and hire paid staff. In January, a group of senators, led by Sen. Sam Brownback, R-Kan., wrote to Secretary of State Hillary Clinton, asking that the money be released.<br />
&quot;It's clear from talking to my friends, both in the State Department and the White House, that one of the concerns that has led to this (delay) is concern about the Chinese reaction,&quot; Palmer told the group of senators and House members on the commission. The State Department did not respond to multiple requests for comment.<br />
Palmer told the congressional panel that if anti-censorship technology like the consortium's could open up enough holes in the &quot;Great Firewall&quot; for enough people, the cyber-gymnastics Google did this week by moving its Chinese search to Hong Kong would be unnecessary, and people could go directly to Google's main search engine. &quot;Google's in a fight and a martyred defeat will not help the cause,&quot; he said.<br />
The consortium provides free encryption software that also allows Internet users to switch IP addresses multiple times a second on a group of dedicated proxy servers scattered around the world, frustrating government blocking or surveillance in any country.<br />
While it is a powerful technology because it is cheap compared with the expensive surveillance and blocking that the Chinese government does, the consortium is a shoestring operation, Huang says. After his daytime job as a software consultant, he works late into the night many evenings for the consortium, and spends his own money on hardware and services. Staffing is all volunteer. The consortium, which also includes Dynamic Internet Technology in North Carolina, has received government funding through the International Broadcasting Bureau, which funds ventures such as Voice of America.<br />
Huang has become a U.S. citizen. But he says there are still reasons to fear the reach of the Chinese government, even here in the Bay Area, and he asked that where he lives and other personal details be kept private. While he applauded the step Google took this week, Huang said that he could not ignore that the company had agreed to Chinese censorship rules for four years.<br />
&quot;To me, I feel it's kind of late,&quot; he said, but added that he hoped the U.S. government would be tougher with China and that other companies would follow Google's lead. &quot;Microsoft should do the same thing; Yahoo should do the same thing; Cisco should do the same thing.&quot;</div>

							
							
							
							
