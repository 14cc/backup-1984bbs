黄云在独家专访中说：“我意识到，如果你害怕，政府就会利用这一点。”<br />
<br />
黄在当地的公司无界网，是加盟全球网络自由联盟中的公司之一。通过使用自由联盟的简单软件，人们可以突破所在国家的封锁和监控，自由地在网上浏览。<br />
