													<h2>CCTV：今年以来全美最大的贪污腐败案</h2>
						
						
																											<div id="postmessage_177232" class="t_msgfont"><a href="http://news.xinhuanet.com/video/2009-07/24/content_11765845.htm" target="_blank">http://news.xinhuanet.com/video/2009-07/24/content_11765845.htm</a><br />
<br />
新华网纽约7月23日电（记者白洁）美国新泽西州特伦顿检察长办公室9月6日宣布，联邦调查局日前端掉了堪称今年以来全美最大的贪污腐败案――包括两名市长和两名州议员在内的11名政府公务员涉嫌公开索贿、滥用职权被捕。这些涉案官员面临着罚金20万美元和监禁20年的严厉处罚！<br />
<br />
市长议员戴手铐脚镣<br />
&nbsp; &nbsp; 当地时间6日下午，新泽西州民主党议员、奥兰治市长米姆斯・哈科特，州民主党议员、帕塞伊克市警察局长奥弗雷德E・斯蒂尔，帕塞伊克市长塞缪尔・里维拉等11名公务员济济一堂。不过，他们不是在开会，而是戴着手铐脚镣出现在特伦顿的法庭被告席上。<br />
&nbsp; &nbsp; 特伦顿检察长办公室透露，11名公务员分别被控以“滥用职权、践踏公民信任和公然索贿”等罪名。<br />
&nbsp; &nbsp;<br />
受贿1500-17500美元<br />
&nbsp; &nbsp; 据负责办案的检察官宣称，11名公务员每次索贿1500美元到17500美元不等，然后以承包公共工程作为回报。这其中，斯蒂尔、哈科特和里维拉分别索贿 15500美元、5000美元和5000美元，然后许诺行贿方可以接下公共项目的保险业务。斯蒂尔的贪欲还要大一些，要求事成之后再给总成交额15%的回扣，而哈科特和里维拉则要求行贿方将25000美元和50000美元分别打到他们的国外账户上。<br />
&nbsp; &nbsp; 美国联邦调查局发言人7日透露说，之所以能掌握如此详实的证据，因为联邦调查局2006年年中时接到举报线索称，欢乐谷校区官员涉嫌集体腐败，为此，联邦调查局设了一个套，成立一家掩护公司，找两名愿意配合工作的公务员，然后让11名贪官直往套里钻。<br />
&nbsp; &nbsp; 根据联邦调查局特工的秘密录音显示，市政官员里德苦口婆心地说服化装成生意人的联邦调查局特工，教秘密特工怎样“更安全”：“想吃独食吗？那么最好不要让厨房里挤满人，因为你可能特别需要奶油什么的！” <br />
&nbsp; &nbsp; 一些贪官根本没有任何的顾虑，比如说联邦调查局最早是打通了欢乐谷校区官员。这个官员在收下秘密特工送上来的几千美元的贿赂后立即将其引见给新泽西州北部的官员，这些官员对送上门来的钱是来者不拒，然后又将特工介绍给更多的官员。就这样，越来越多的贪官纳入联邦调查局的视线。<br />
&nbsp; &nbsp; 当然，更多的官员就显得出奇地谨慎，他们在索贿时甚至满嘴暗语，比如说2006年11月，其中一个名叫索托的官员给联邦调查局的这家掩护公司发传真，在问所索的贿赂是否到账时用的词语是：“蛋糕的事有说法没？”2006年12月，索托又给掩护公司的另一个人发传真说：“我现在急需椰菜。”<br />
&nbsp; &nbsp; 一旦收受了贿赂之后，官员表现出的积极性也非同小可。比如说当一名市政的官员向帕塞伊克市长塞缪尔・里维拉质疑联邦调查局掩护公司资质时，里维拉警告他说：“与这家公司的合作决定是我和市政委员会做出的，相信我吧，我已经争取到四张支持票，你就别问三问四的了，赶紧让项目动起来吧。”<br />
&nbsp; &nbsp;<br />
20年监禁加20万保证金<br />
<br />
&nbsp; &nbsp; 这个贪污腐败窝令联邦调查局异常震惊。<br />
&nbsp; &nbsp; 负责此案的联邦调查局特工说：“我想对贪官和想贪的官员说，你们在下手之前得好好掂量掂量。那些滥用职权和公信的官员在贪污之前最后摸摸自己的口袋。我也要劝劝那些自以为找到聪明办法收受贿赂的人，或者认为贪污一点不算什么的人：在美国，没有贪污腐败的天堂！”<br />
&nbsp; &nbsp; 事实也是如此，11名贪官的保证金高达每人20万美元，并且如果他们指控的罪名成立的话，那么每人将面临着20年联邦监狱的监禁。<br />
<br />
“疾病”传染新泽西<br />
<br />
&nbsp; &nbsp; 案情公开后，新泽西州检察官克里斯托弗J・克里斯蒂感慨地说：“今天，我们亲眼目睹了一种传染整个新泽西州的病。这个病就是腐败，它就像野火一样从南传到北。”<br />
&nbsp; &nbsp; 其中一名贪官的上司也感叹道：“我这份工作其实是象征性的。说句真话，我真不敢相信我的手下会贪污，我非常震惊，因为我不知道这个位置怎么去影响一个市政的决定。”说着说着，这名女官员居然落泪称：“别让我哭出声来。”<br />
&nbsp; &nbsp; 当媒体纷纷打电话到当事人办公室，试图证实他们贪污腐败的事时，各方均选择了回避，或者拒绝评论。<br />
&nbsp; &nbsp; 新泽西州议长约瑟夫・小罗伯特表示，他对两名州议员因腐败被逮捕感到“恶心”：“这是新泽西人民悲伤的一天，因为本州的贪腐文化再度成为全国性的新闻。”<br />
&nbsp; &nbsp; 议长并没有说得过分，因为这次被捕的欢乐谷市政官员彼得・卡拉威，其哥哥是大西洋城的市政高官，因为另一桩腐败而被判有罪，眼下被关押在联邦监狱里。对此，负责监督当地财政开销情况的州教育部监督员约翰・德塞拉博尔表示：“对于欢乐谷来说，这是可怕的一天，是我们大家都不需要的又一重大打击。”<br />
&nbsp; &nbsp; 事实上，在最近的五年间，新泽西州先后有100多名公务员因贪污腐败被逮捕，其中包括两名资深的民主党议员。<br />
<br />
<br />
新闻观察：美国反腐败机制揭秘<br />
<br />
&nbsp;&nbsp;美国实行联邦政府体制，设立联邦、州及地方三级政府，每级政府都建立了相似的廉政机构和廉政法规体系。其反腐败运行机制主要有三大特点：<br />
<br />
&nbsp; &nbsp; 1．独立性和权威性<br />
<br />
&nbsp; &nbsp; 美国反腐败机构众多，网络比较健全。从系统上看，立法、司法和行政部门都设有反腐机构负责本系统的廉政工作。从职能上分，一类侧重于制定和实施廉政准则和廉政计划，着重预防腐败；另一类则是调查、处理腐败案件，着重惩治腐败。一方面，反腐机构的职能、权限和工作程序在法律上都有明确规定。另一方面，反腐败机构的层次较高、工作范围较广。如联邦调查局是专职调查危害国家安全和公共利益的机构，调查范围极其广泛，且不受地域限制，有权调查200多种犯罪案件，其中包括政府官员贪污及其他腐败案件。<br />
<br />
&nbsp; &nbsp; 2．系统性和综合性<br />
<br />
&nbsp; &nbsp; 腐败问题不仅是人品问题，而且是制度问题，必须在加大惩治力度的同时，运用经济、行政、法律、制度等综合手段予以防治。<br />
&nbsp; &nbsp; 比如说每名公务员进入政府部门时，由部门廉政官员进行至少1个小时的廉政规定培训，使其明确哪些可以做、哪些不可以做，以及为其提供反腐败的有关信息、咨询、联系方式，针对不同行业、单位和不同岗位、职位提供相应的反腐败措施以及廉政忠告。政府廉政办公室负责对各部门廉政官员的培训，并对各部门开展廉政教育进行指导。一方面，美国以严厉手段惩治腐败行为。另一方面，以严格监督防范腐败行为。美国反腐机构认为，阳光就是最好的防腐剂。<br />
