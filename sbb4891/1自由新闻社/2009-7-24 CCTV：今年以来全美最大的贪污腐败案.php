&nbsp;&nbsp;3．国际性和民间性<br />
<br />
&nbsp;&nbsp;1975 年美国出台了《外事法案》，禁止美国公司送给外国官员金钱，要求在美国上市的国内外公司财务运作必须公开，这部法案适用于美国海外公司和国内任何公司以及为公司效力的人员。在美国推动下，1997年5月世界经济合作与发展组织出台了共同反对国际贸易中的行贿条约，33个国家都签订了该条约，由此，限制企业行贿成为国际惯例。<br />
&nbsp; &nbsp; 美国反腐败趋势呈现出国际化和监督力量民间化的显著特征，民间机构包括研究机构、咨询机构等在反腐败中日益发挥重要作用。此外，民间媒体越来越多地采取新闻调查等方式参与对政府的监督。舆论监督权在美国是被誉为独立于行政、立法、司法之外的第四个权力。（春雨）</div>

							
							
							
							
