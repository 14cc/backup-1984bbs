　　身着海军军服的王震<br />
<br />
　　<strong>扬子晚报10月1日报道</strong> 在参加阅兵的海军岸舰导弹方队中，有一位24岁的江苏大丰籍战士――王震。今天上午，他将和战友一起自豪地穿越天安门广场，接受检阅。但这一刻他还不知道，年仅48岁的父亲早在8月的一次车祸中就不幸罹难，再也无法看到儿子的英姿。是他的母亲一直向儿子隐瞒着真相，含泪将丈夫遗体从千里之外运回家乡，静静地等待儿子归来……<br />
<br />
　　大丰市飞轮厂宿舍区8号楼402室，就是海军战士王震的家。客厅里，青纱挽着父亲王国祥的遗像，墙上大大的“奠”字让这处仅有46平方米的简陋居室充满了悲伤。而远在北京阅兵村的王震还一直满心欢喜地等待着10月1日上午父亲能如约在电视机前观看自己的参阅方阵。<br />
<br />
　　昨天一大早，王震的母亲袁亚萍在丈夫的灵堂前摆上一碗饭、敬上一炷香，对着丈夫遗像轻声自语：“国祥，原谅我50多天来对儿子隐瞒真情，明天他就该知道你不在了，你也能入土为安了……”早在几天前，袁亚萍就将电视机从卧室搬到丈夫灵堂前，好让丈夫和她一起“观看”，满足丈夫生前的愿望。<br />
<br />
　　<strong>走进阅兵村，父子有个美好约定</strong><br />
<br />
　　5年前，当儿子成为一名海军士兵，父亲王国祥高兴得彻夜难眠：当兵，曾是他从小的梦想，儿子终于帮自己实现了。在父亲的鼓励下，王震当兵后表现优异，3次被评为优秀士兵，一次被评为优秀士官。<br />
<br />
　　去年年底，王震兴奋地给家里打来电话，说大阅兵，自己被选拔上了!这一消息让王国祥夫妇欣喜若狂，王国祥激动地抓着电话半天不知说什么好，最后说了一句：“儿子，能参加阅兵，是你一生的光荣，一定要好好训练。”<br />
<br />
　　今年上半年，王震同千挑万选出来的战友一道进驻北京阅兵村集训。从那天起，父亲就和王震立下一个美好约定：国庆这天上午，父亲什么都不干，就守着电视观看儿子豪情万丈地穿越天安门广场。儿子参加国庆阅兵，也让一向木讷的王国祥变了一个人，他遇到熟人总要自豪地说：“我儿子今年要参加国庆阅兵啦，不简单哪!”<br />
<br />
　　袁亚萍告诉记者，儿子参加国庆大阅兵，也给了丈夫莫大的鼓励，他告诉儿子，自己要出去打工挣钱，等儿子退伍回来就给他买一套房子，让儿子以后在新买的房子里结婚。<br />
<br />
　　但让人万万没想到的是，今年8月8日一大早，王国祥带着腰伤同3名老乡一起，离家前往山东青岛的一处工地找活干。到了工地，王国祥发现人手不够，当晚7点多，王国祥等人准备拦车回家再招人。在同三高速公路青岛段，王国祥和另一名老乡惨遭车祸，两人被一辆轿车撞飞，王国祥当场死亡。<br />
<br />
　　<strong>“什么都可以没有，就是不能没有爸爸”</strong><br />
<br />
　　8月9日凌晨，王国祥遇难的噩耗从山东传到大丰，执手相牵了20多年的丈夫突然去世，让袁亚萍痛苦万分。此刻，她首先想到的是儿子，国庆大阅兵一天天临近，他能否请几天假回来为父亲送行?袁亚萍辗转联系上了王震所在部队首长，部队领导商议后回复袁亚萍：集训进入最关键时刻，战士一个不能少!袁亚萍知道，阅兵是国家大事，也是丈夫和儿子共同的期盼，再大的事也不能影响训练。她果断地告诉部队首长：“既然王震不能回来，那也不要告诉他父亲已经去世。”两人商议决定，只对王震说父亲遭遇车祸受伤。<br />
<br />
　　得知父亲受伤后，王震还是心急如焚，他不断打电话给妈妈，追问父亲的情况，每次接听儿子的电话，袁亚萍都心如刀绞，她总是捂住嘴，生怕哭出声来。“妈，你的喉咙怎么哑了?”“爸爸怎么不接电话?”袁亚萍尽力平静地告诉儿子，她只是受凉了，爸爸还在昏迷，不能讲话。<br />
<br />
　　为了给父亲“治病”，王震向战友借了1万元寄回家来，并反复叮嘱母亲：“妈，一定要给爸爸找最好的医生，哪怕把家里房子卖掉也要让爸爸好起来。我什么都可以没有，就是不能没有爸爸。”<br />
<br />
　　虽然部队首长、妈妈和所有亲戚都说他父亲只是受了伤，但细心的王震似乎有了些不好的预感。一次，王震在电话里对着母亲失声痛哭：“爸爸千万不能走，他还要看国庆阅兵呢。”他追问母亲“你能肯定爸爸没事吗?”并恳求母亲：“你答应我，一定要交给我一个完整的爸爸!”<br />
<br />
