<br />
　　她把自己的愿望告诉了部队领导以及大丰市人武部、民政局。部队、人武部和民政局都十分重视，并分别给青岛交警发函，请求警方能特事特办。对此，当地交警也十分为难，袁亚萍几次跪倒在处理事故的交警面前，最后，山东警方破例准许王国祥的遗体运回江苏。<br />
<br />
