<br />
　　8月13日，袁亚萍将王国祥遗体从千里之外运回家乡，存放于当地殡仪馆，并在家中的客厅里为丈夫搭建了简易的灵堂，她尽量少让人知道丈夫去世的消息，甚至连一些亲属也没告诉，自己则常常孤独地守着丈夫的遗像直到天亮。青烛长明，这位人至中年的普通妇女，忍受人间最难熬的50多个日日夜夜，一向不沾香烟的袁亚萍，竟然抽掉了近两条香烟。<br />
<br />
　　9月21日，记者第一次来到袁亚萍家时，恰巧王震给妈妈打来电话询问父亲的状况，袁亚萍立刻抹掉满脸泪水，强作兴奋地告诉儿子：“你爸爸渐渐有知觉了，手也能动了，我现在正抓着你爸爸的手呢……”而这时，她手里握着的却是丈夫遗像的一角。听到这个“好消息”，王震在电话中居然开心地大笑起来：“好，这就好!你在爸爸耳边不停地叫我的名字，跟他说阅兵的事，他听了一定能苏醒过来!”<br />
<br />
　　袁亚萍告诉记者，在自己面对的现实中，丈夫早已离开了人世，但在儿子的世界里，父亲还活着。每接听儿子一次电话，对这位坚强的母亲来说，都无疑是一次生与死的痛苦煎熬。丈夫遗体运回来后，她曾经告诉儿子，父亲没有生命危险，只是脑部有淤血，做完两次手术就好了。为此，过几天袁亚萍又要编一个谎言，说第一次手术非常顺利。但常常精神恍惚的她，说过要做两次手术，接着又把这“事”给忘了。等到儿子又打来电话，追问父亲怎么还不做第二次手术时，猛然想起此“事”的袁亚萍只好搪塞：现在做还有危险，要过一段时间才行。<br />
<br />
　　9月23日上午，袁亚萍收到了儿子从北京专门给父亲寄来的包裹，里面有海军衬衫和军皮鞋以及国庆纪念衫，还有一只北京烤鸭。袁亚萍则对儿子说：“现在你爸爸能喝鱼汤了，阅兵时肯定能看到你。”对儿子说完这“好消息”，袁亚萍放下电话后放声大哭，她无法想象大阅兵过后，怎样面对蒙在鼓中的儿子?和父亲如此亲热的儿子，一旦得知父亲早就去世又将是如何地悲痛!<br />
<br />
　　一个普通的母亲所编织的“悲情谎言”，也深深感动了王震所在部队，部队首长给袁亚萍寄来了慰问金。袁亚萍告诉记者，9月28日，她和部队首长进行了交流，部队也已悄悄为王震买好了4日回家的机票。<br />
<br />
