国学大师、楚辞研究者文怀沙先生自称生于1910年，为百岁老人。学者李辉攻击，考其档案，实生于1921年，文先生多说一轮。又批，学问空空，文章造假。两相争论，舆论大哗。敝人不堪寂寞，搜肠刮肚，得诗三首，以凑热闹。<br />
