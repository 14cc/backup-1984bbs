　　＂ ■反映：空调车暗藏“杀机”<br />
　　<br />
　　 “空调车上没有救生锤，一旦遇到紧急情况，后果将不堪设想！”近日，市民王先生打进本报热线电话说，成都市大多数空调车上救生锤架子上的救生锤都不见踪影，有的甚至连救生锤架子都不见了。<br />
　　<br />
　　 他表示，如果这些公交车一旦遇到紧急情况（如突然发生大火），车门在一时无法打开的情况下，只有后排的车窗能够逃生，大部分乘客就会因为没有救生锤而无法砸碎玻璃逃生。<br />
　　<br />
　　 据了解，救生锤是挂在空调客车侧面玻璃旁边的尖形铁锤，主要用于乘客遇到紧急情况（汽车燃烧、溺水等）时砸碎玻璃逃生。<br />
　　<br />
　　 王先生还说，公交车在营运中本身就已超载，尤其是遇到上下班高峰期，车上被挤得一点缝隙都没有了。一旦发生类似广西横县境内的特大汽车火灾，如果车上没有救生锤，所有人拥挤起来，逃生的机会就会更少，后果是不堪设想的。<br />
　　<br />
　　 ■调查：救生装置形同虚设<br />
　　<br />
　　 为核实王先生反映的情况，近日记者对成都市部分空调公交车进行了调查。<br />
　　<br />
　　 ４月４日下午５：００左右，在成都市武成大街公交车站，记者费尽了力气挤上了一辆车牌号为川Ａ４７２５９的３４１路空调公交车。<br />
　　<br />
　　 几经周折后，记者终于通过人缝，在靠近车身前方的位置发现了一个红色的座子（挂救生锤用的），但遗憾的是该座子上并没有救生锤，呈现在记者面前的只是一个空架子。<br />
　　<br />
　　 在该辆空调公交车上，记者又在离车尾大约１米左右的玻璃之间发现了另一个救生锤底座，但该底座上仍然没有救生锤。随后，记者又费劲地向前移动、搜索，终于在车身前部售票员座位后面的玻璃之间，发现了一把红色的救生锤。<br />
　　<br />
　　 在记者调查中，这辆车牌号为川Ａ４７２５９的３４１路空调公交车还算情况比较好，而在４月５日到６日期间，记者调查的３８路、６１路、３４路、６２路、２７路、５６路等多个公交线路的２０余辆空调车中，除个别空调车上有３个以上的救生锤外，许多空调公交车上几乎没有救生锤，有的甚至连底座都没有（如川Ａ４９５６７的６２路、川Ａ５０８７２的５６路、川Ａ４９６４６的５６路、川Ａ４７６９４的５６路、川Ａ４５８８４的３４路等）。<br />
　　<br />
　　 ■售票员：多数不知救生锤<br />
　　<br />
　　 ４月６日下午４点３０左右，记者登上车牌号为川Ａ４７６９４的５６路空调公交车，就救生锤问题咨询了该车售票员。<br />
　　<br />
　　 这位售票员表说：“这个我不是很清楚，我第一次在这辆公交车上工作的时候就没有看到过救生锤！”<br />
　　<br />
　　 随后，记者又问该售票人员救生锤的作用时，该售票人员告诉记者她不并清楚，不知道是用来做什么的，公交公司也没有给他们介绍这方面的有关常识。<br />
　　<br />
　　 在调查中，除个别车次售票员外，多数售票员都不了解救生锤及其作用。<br />
　　<br />
　　 ■乘客：我要自己携带铁锤<br />
　　<br />
　　 “我今后要自己随身携带一个铁锤！”４月６日，市民王先生半开玩笑半认真地对记者说，正因为他看到３月１日发生在广西横县车祸的相关报道，才注意到空调车上很少有救生锤。<br />
　　<br />
　　 王先生表示，事情没有发生的时候，大家都存在侥幸心理，很多人（包括公交公司人员）都会觉得公交车只是在城市里开，有１１９救援。“要知道，一旦发生广西那种紧急情况，等１１９赶到，恐怕多数人都已遭不测了！”<br />
