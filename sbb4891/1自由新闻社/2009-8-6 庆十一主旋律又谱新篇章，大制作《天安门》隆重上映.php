													<h2>《天安门》9月3日上映 叶大鹰批当红明星自大</h2>
						
						
																											<div id="postmessage_193671" class="t_msgfont">网易娱乐8月4日讯（文/3pinky 图/宋爽）2009年正是共和国建国六十年大庆，中影集团在国庆档将有两部重头献礼片：《建国大业》以抗战胜利至建国前夕为背景，再现多党合作和政治协商制度的成长历程；《天安门》则从几个普通战士的视觉重现激情年代。影片将于9月3日全国公映，今日在北京举行海报、预告片发布会，导演叶大鹰、主演潘粤明、郭柯宇、徐啸力等亮相。<br />
<br />
《天安门》描述49年建国前夕，开国大典舞台在28天里搭建的浩大工程，影片也“冒险”让毛主席本人和主演同台“演出”，在发布会上叶大鹰揭秘这段“穿越”的镜头是如何实现的；《建国大业》、《风声》、《十月围城》等献礼片大打明星牌，《天安门》主演阵容则相当低调，提到大片必用大牌的风气，叶大鹰称自己有抵触心理，他直言并不喜欢现在的当红明星：“一上来就说档期，这不是自大狂吗？”“我需要的是踏实的好演员，经得起拍摄这部影片的跌荡。”<br />
<br />
<br />
叶大鹰批明星制不利电影创作 不把张静初当大牌<br />
<br />
作为叶大鹰“红色三部曲”收尾之作，《天安门》演员阵容出现了《红樱桃》主演“楚楚”郭柯宇和“罗小蛮”徐啸力，潘粤明则担纲男一号，饰演负责翻修天安门城楼的舞美队队长。相比叶大鹰上一部作品《红色恋人》里张国荣的熠熠星光，《天安门》的阵容远不够显赫，当记者问道为何不像其他献礼片启用大牌明星时，叶大鹰直言对明星制没有信心：“我最近不喜欢当红的明星......一上来就谈档期,这不是自大狂吗？”他坦诚《天安门》的拍摄难度来自还原49年天安门的制作和特技制作过程，在拍之前自己心里也没底；与其找明星，叶大鹰称自己更想和踏实的好演员合作：“他们会花很多时间去做创作准备，比如课程，排练。他们得跟我一起折腾，经得起这个影片的跌宕和不顺利，如果拍不好，得反复倒腾。”<br />
<br />
<br />
郭柯宇的角色原本属意张静初，却正是因为后者档期不合无法合作，叶大鹰笑称自己已经尝过明星档期限制的苦头，但心里并不把张静初当明星看待，“她人挺好。找她演这个角色，是因为这个角色需要会拉手风琴，《孔雀》里她拉手风琴的戏演得很好。”<br />
<br />
<br />
叶大鹰对大牌滥用风气的批评，让人不禁想到这是否在影射同期上档、拥有破纪录式明星阵容的《建国大业》。叶大鹰解释道，《建国大业》是特殊情况，“这就像给老爸老妈过大寿，怎么贴金都不为过，有一部影片可以把那么多明星集结在一起，包含了那么多演员和创作人员对祖国的感情，是非常了不起的事情。他也称《建国大业》带来的快乐来自观众数明星的兴奋，而《天安门》则是用故事本身温暖观众。<br />
<br />
<br />
潘粤明和毛主席片中“对戏” 伤势减轻已复工<br />
<br />
在发布会上，潘粤明被主持人称作最幸运的人，因为在《天安门》里，他不但和毛主席本人“握手”，还站在了毛主席身旁，这段开国大典上的镜头并未找来特性演员扮演毛主席，而是用数字技术加替身，将毛主席的真人形象和电影情节以假乱真的结合在一起。叶大鹰从《阿甘正传》里得到了灵感，而在建国60周年，让银幕上“复活”毛主席，他相信这种视觉冲击对观众是很大的惊喜。但这段镜头也是让叶大鹰感到最费劲的部分：“现在能找到的毛主席的影像资料质量很差、数量很少，还要符合剧情。”在制作这组镜头时，叶大鹰笑称还要避免被十几年被古月等演绎的毛主席形象影响：“第一次做的画面把我自己惊到，看多了特型演员的造型，反而觉得真的毛主席‘怎么不像唐国强啊’。得把他们都忘了，心里只想着毛主席。”<br />
<br />
<br />
刚经过一场生死劫难的潘粤明也现身为影片造势，稍微有些发胖的他称目前身体复原得差不多，肋骨还没长好，但已经恢复正常工作。叶大鹰称自己需要能经得起拍摄波折的演员，潘粤明也身体力行地证明了自己是合适的选择，在刚出院的时候他在行动不方便时仍配合剧组补拍一组主角老年的照片，但他也称经过这次意外，董洁会限制自己不接有高危动作的戏：“热爱工作之余也要在安全范围之内。” (本文来源：网易娱乐专稿 作者：3pinky)<br />
