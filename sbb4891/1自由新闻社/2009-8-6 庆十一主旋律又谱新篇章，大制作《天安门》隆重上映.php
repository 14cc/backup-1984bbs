<a href="http://ent.qq.com/a/20090806/000152.htm" target="_blank">http://ent.qq.com/a/20090806/000152.htm</a><br />
　　由叶大鹰执导的电影《天安门》已敲定9月中旬登陆全国院线。昨日，叶大鹰率主演潘粤明、郭柯宇等在京出席官网启动及海报、预告片发布会。遭遇车祸后首次露面的潘粤明成了全场焦点，明显发福的他透露身体已无大碍，即将复工。据叶大鹰导演介绍，片中的毛主席影像均来自真实历史资料，用数字技术修复加工而成。<br />
<br />
　　叶大鹰十年圆一梦<br />
<br />
　　《天安门》讲述了1949年9月2日，华北军区抗敌剧社舞美队队长(潘粤明饰)接到紧急任务，毛主席和党中央决定10月1日在天安门举行开国大典，要求舞美队在28天的时间里对开国大典的主席台即天安门城楼进行整修和布置的故事。<br />
<br />
　　叶大鹰说，他于1997年筹备《红色恋人》时，被一张天安门的老照片激发了创作的灵感，但当时觉得自己是在做白日梦。1999年，他本想拍摄此片献礼建国50周年，但因拍摄《红色恋人》分身乏术，一直未能如愿，十年后，他终于圆了自己的梦。<br />
<br />
　　天安门是中国的象征，差不多每个人心中也都有一个“天安门情结”。如何通过电影来展现这个雄伟建筑，叶大鹰透露，剧组在横店建了一个与天安门1：0.9比例的旅游景点，遗憾的是因为前面没有广场，不能拍摄俯瞰天安门的场景。<br />
<br />
　　毛主席影像剪进影片<br />
<br />
　　叶大鹰说，在新中国60华诞之际，让观众在银幕看到真实的毛主席影像，是件具有特殊意义的大事：“毛主席的镜头是我们从他在几个时期留下的影像资料上用数字技术修复加工而成的。1949年拍摄开国大典的苏联纪录片小组的资料被烧毁了，我们能找到的影像资料大多数是上世纪六十年代及七十年代的画面，技术上很难完全统一在1949年的形象上，我们现有的资料和技术只能‘还原’六十年代的毛主席，因为留下来的资料片都是翻了好多遍拷贝，技术难度和风险都很大。各方面原因使‘重现毛主席’变成了一件很不实际的事情。但是，我们的决心已定，必须把毛主席的形象做出来。如果不成，就等于这部羽翼丰满的电影折了一只翅膀，我都不能原谅自己。”<br />
<br />
　　最终，先进的电影科技将十年前被叶大鹰认为是“白日梦”的创意变成了现实。<br />
<br />
　　潘粤明发福复工<br />
<br />
　　因拍摄电影《为你而来》而遭遇车祸的潘粤明许久没有露面了，休养期间调养有道，他明显有些发福：“当时肋骨断了，把肺刺穿了，现在不能抽烟，只能天天煲汤喝，人一下子就‘吹’起来了。”因为骨头还没有完全长好，潘粤明不敢轻言减肥。“身上胖了无所谓，但演员毕竟是靠脸吃饭的，脸胖了就比较麻烦，等我的骨头完全好了，就去健身房。”<br />
<br />
　　回忆车祸瞬间的感受，潘粤明称当时并不害怕，因为正在拍摄塌方的戏。“ 但经过这件事，我在思想上有了升华，发现活着才是最好的，能和家人在一起，其他都不重要了。”谈及受伤期间妻子董洁无微不至的照顾，他一脸甜蜜地说：“挺开心的。”<br />
<br />
