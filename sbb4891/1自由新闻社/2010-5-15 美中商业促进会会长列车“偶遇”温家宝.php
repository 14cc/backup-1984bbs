																											<div id="postmessage_616501" class="t_msgfont">今天下午在从北京去天津的火车上，遇到了温总理，而且他就坐在我的对面。<br />
<img src="http://thumbsnap.com/i/ptREu4uJ.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
（总理在倾听）<br />
我乘坐的是5月13日下午2:30从北京南站开往天津的火车，去天津机场和一家大的航空公司洽谈与美国匹兹堡货运直航的事情。在站台上，有很多的保安，几节车厢都不让人靠近，看来是有大人物坐车。上了车，邻座的乘客抱怨因为保安措施入站买票的不便。中国的官和民的等级就是分明。不过大人物是谁，大家也不知道。<br />
火车刚开几分钟，从前面车厢就走过来一大群人。坐对面的人说，一个人好像是铁道部长。人群慢慢走近，走在中间的竟然是温家宝总理！<br />
乘客们见到总理都很激动，总理频频和乘客握手交谈。不一会，总理走到我的座位附近，我赶忙与温总理握手，总理的手亲切而有力。<br />
<img src="http://thumbsnap.com/i/2GeA9ZjW.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
（和群众握手）<br />
温总理穿着朴素，身着一个灰色夹克衫，面容慈祥亲切而庄严。总理快七十了，精神非常好，思维敏捷，而乐于倾听。<br />
总理和周围的乘客亲切握手交谈，问候。他见到我们这个坐厢有一个空位子，说，我坐下吧。他就坐在我的对面。四个人围坐一个方桌。我不敢相信，做在我对面的就是总理。<br />
<img src="http://thumbsnap.com/i/zREnWZ7R.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
（总理坐在我的对面）<br />
新疆伊犁的一个回族的农民工大爷过来，向总理问好。总理问，你怎么不在家乡打工，选到天津？农民工大爷说，我们农闲时就到外地打工。一个当地的老板在天津有项目，是南水北调工程的一部分，就来了。这是第二年来天津了。<br />
<img src="http://thumbsnap.com/i/EyKAS8HK.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
（总理和新疆农民工交谈）<br />
<img src="http://thumbsnap.com/i/AzHRqCJs.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
（和群众握手）<br />
我对总理说，我定居美国，看到祖国的经济稳定高速发展。我们做为华人感到十分自豪。我做为美中商业促进会的会长，也很愿意做中国交流和加强理解的工作。中国的发展，也真是一个大考验，这体现了中国人民的勤奋和智慧。<br />
温总理说，是呀。美国总统说，美国有600万人失业，压力很大。我们中国当前农民工就有1.5亿人。我们要为2亿人找到工作。中国的发展，更加需要加倍努力。<br />
总理看着窗外，经过一片农田。总理说，去年全球气候反常，冬天又冷又长，今年的农忙季节要抓紧，施肥育苗，还能保证好的收成。总理说，下周西南又要下大雨了。谈及此，总理眼望前方，露出忧郁之色。我不由想到上个月，见到总理视察灾区，蹲在云南干旱大地上的照片，那是同样的忧郁之色。<br />
本次列车的列车长，北京地质大学的老校友，北师大的二年级学生，都纷纷过来，问候总理。总理的思维十分敏捷。对各个行业，都有很深的了解。<br />
坐在我旁边的乘客要总理签名，总理笑着说，“还是不要签了吧，一签就得半个小时。”大家也珍惜和总理交谈的时间，就不再要求了。火车快到站了，远处一个乘客过来，要求总理签名，这次总理竟答应了。乘客实在激动，找不到纸了，拿出车票，总理笑着说，”这不好吧,我们找一张白纸。“总理拿过纸，认认真真地写，“温家宝”。每个字都写得很慢很认真。<br />
<img src="http://thumbsnap.com/i/JINXqXBZ.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
（总理在签名，用的是我的笔，后面铁道部长也要把自己的笔递过来）<br />
<img src="http://thumbsnap.com/i/AwoGxJk9.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
（和美女列车长交谈，旁边是铁道部长）<br />
火车要到站了，总理离去，大家全体鼓掌。<br />
温总理亲切得就像一个老师长，让我竟一时忘了这是一个管理十几亿人大国的国家总理。温总理果然是亲民的总理。<br />
下午突然想起来，我去年4月底，在美国给温总理写了一封关于中国地产的信，谈了自己对中国地产一些问题的观点。今天见到了温总理，居然忘了提起了。不过那一封信，其实是写给地产业界决策者的，不知温总理看到没有。今天，见到的是温家宝总理本人，他坐在对面，耐心倾听，侃侃而谈，就是一个亲切的老朋友。<br />
&nbsp; &nbsp;衷心祝愿亲政爱民的温总理身体健康，祖国繁荣昌盛。</div>

							
							
							
							
