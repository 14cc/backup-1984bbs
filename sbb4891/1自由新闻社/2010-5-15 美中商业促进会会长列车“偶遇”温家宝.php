													<h2>网友博客实录动车上巧遇温总理（组图）</h2>
						
						
																											<div id="postmessage_618748" class="t_msgfont">【编者按】 昨日凌晨，某网网友陈航在其博客上发布博文“回国见闻――巧遇温总理”，引起各网站的转载。以下为博文全文，小标题为本报所加。<br />
<br />
总理在我对面坐下<br />
<br />
今天（编者注：5月13日）下午在从北京去天津的火车上，遇到了温总理，而且他就坐在我的对面。<br />
<br />
我乘坐的是13日下午2:30从北京南站开往天津的火车。在站台上，有很多的保安，几节车厢都不让人靠近，看来是有大人物坐车。<br />
<br />
火车刚开几分钟，从前面车厢就走过来一大群人。人群慢慢走近，走在中间的竟然是温家宝总理！<br />
<br />
乘客们见到总理都很激动，总理频频和乘客握手交谈。不一会，总理走到我的座位附近，我赶忙与温总理握手，总理的手亲切而有力。<br />
<br />
温总理穿着朴素，身着一个灰色夹克衫，面容慈祥亲切而庄严。总理快七十了，精神非常好，思维敏捷，乐于倾听。<br />
<br />
总理和周围的乘客亲切握手交谈，问候。他见到我们这个车厢有一个空位子，说：“我坐下吧。”我不敢相信，坐在我对面的就是总理。<br />
<br />
“你怎么不在家乡打工”<br />
<br />
新疆伊犁的一个回族农民工大爷过来，向总理问好。总理问，“你怎么不在家乡打工，选到天津？”农民工大爷说，“我们农闲时就到外地打工。一个当地的老板在天津有项目，是南水北调工程的一部分，就来了。这是第二年来天津了”。<br />
<br />
我对总理说，“我定居美国，看到祖国的经济稳定高速发展。我们华人感到十分自豪。我作为美中商业促进会的会长，也很愿意做中国交流和加强理解的工作。中国的发展，也真是一个大考验，这体现了中国人民的勤奋和智慧。”<br />
<br />
温总理说，“是呀。美国总统说，美国有600万人失业，压力很大。我们中国当前农民工就有1.5亿人。我们要为2亿人找到工作。中国的发展，更加需要加倍努力。”<br />
<br />
总理看着窗外，经过一片农田。总理说，去年全球气候反常，冬天又冷又长，今年的农忙季节要抓紧，施肥育苗，还能保证好的收成。总理说，下周西南又要下大雨了。谈及此，总理眼望前方，露出忧郁之色。我不由想到上个月，见到总理视察灾区，蹲在云南干旱大地上的照片，那是同样的忧郁之色。<br />
<br />
给旅客签名：每个字都写得很认真<br />
<br />
坐在我旁边的乘客要总理签名，总理笑着说，“还是不要签了吧，一签就得半个小时。”大家也珍惜和总理交谈的时间，就不再要求了。火车快到站了，远处一个乘客过来，要求总理签名，这次总理竟答应了。乘客实在激动，找不到纸了，拿出车票，总理笑着说，“这不好吧，我们找一张白纸。”总理拿过纸，认认真真地写，“温家宝”。每个字都写得很慢很认真。<br />
<br />
火车要到站了，总理离去，大家全体鼓掌。<br />
<br />
温总理亲切得就像一个老师长，让我竟一时忘了这是一个管理十几亿人大国的国家总理。温总理果然是亲民的总理。<br />
<br />
如此近距离面对面和温家宝总理交谈，真是巧遇，真是有幸。衷心祝愿亲政爱民的温总理身体健康，祖国繁荣昌盛。<br />
