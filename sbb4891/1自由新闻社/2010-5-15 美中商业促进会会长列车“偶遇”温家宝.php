																											<div id="postmessage_616870" class="t_msgfont">作者叫陈航，美中商业促进会的会长<br />
《回国见闻－巧遇温总理》后记<br />
读者的反馈反馈也很多，这里作答几则。<br />
<br />
我为什么要写“巧遇温总理”这篇文章？<br />
总理亲民勤政，从一个平民的笔写出来，有感而发。<br />
记录一段历史，也是我的责任。<br />
<br />
你见过中国的领导出门坐火车的吗？如果真的坐的话，那保镖去哪了？<br />
我这次见到了。<br />
保镖在后面车厢里，站台上，估计车上随从不下200，站台上保安人员不下500. 到了天津，出租司机说，火车站所有的主要道路都封了。<br />
不过车厢里，保镖不是很多。<br />
<br />
我是不是演员？<br />
不是。我从来没有演过戏。倒是想试试，没有导演来找我。<br />
<br />
群众都是事先挑选好的？<br />
不是。<br />
我就说详细一点，买票的过程，说明我不是事先挑选的。<br />
我在国外就听说京津的动车高速，一直是听说过没坐过。这回去天津和一家大的航空公司洽谈开通美国空运直航的事。四点开会，我中午吃完饭就走了。<br />
我坐地铁来到北京南站，先到一层购票厅买票，排队的人很多。显示板上说去天津可以直接在二楼买票，我就上二楼了。不想二楼很多的保安，说是自动售票今天暂时停了，只能到一楼去买。我又返下楼。排队。当时买票人多，一个窗口的售票员喊，马上去天津的来买票。我就到前面去买了。好像是59元。当时离开车时间不到10分钟。如果我是事先挑选的，至少应该给我免票吧。<br />
<br />
我旁边的一个北京做旅游的乘客也不是事先挑选的。他上车还抱怨高官扰民，盼望火车不要因此晚点，而且回来的时候可不要再遇到高官同车。温总理坐到他的对面后，他的手机响了，他接起电话还说,“我现在不能说话，太激动了。”<br />
<br />
我对面的旅客朋友也不是挑选的。他昨天还发短信来向我要照片。照片太大，我回美国再发吧。<br />
我们三个和总理坐在一个客厢的乘客全都不是挑选的。其他走上前的乘客凭我的判断也不是。<br />
估计只有列车长来问候总理是铁道部长事先安排的，不过这是礼节上的事。<br />
<br />
我有没有被事先政审？<br />
估计没有。除非中国政审的效率是30秒钟在买票的时间内完成。我如果在美国是恐怖分子，北京南站估计也很难在几分钟内知道。<br />
<br />
我有没有被事后政审？<br />
可能有。我一共给出了三张名片，一张给温总理，另外两张是温总理走后他的随行人员要的。估计是查一下我有没有不良前科，动乱表现什么的。<br />
<br />
关于名片的细节<br />
温总理还仔细读了我的名片，正反面都看了。还说，“美中商业促进会的会长呀”。让我很不好意思。<br />
总理是个认真的人。<br />
<br />
网友问我为什么我没有抓紧时间，问总理贪官，西藏，宗教，杀童，误判的问题？<br />
我个人认为，中国很大的问题很多，令人瞩目大的发展机会也有很多。所以在短暂的会面中，我还是选择后者。所以我的确向总理请教了关于知识产权保护，外资公平，和农业的问题。<br />
总理坐在对面，更像是老师长。<br />
这不是记者招待会。<br />
<br />
总理来和大家坐火车就是亲民吗？是作秀吧？<br />
我看是就这个具体的事是挺亲民的，至少也是认真和亲切。<br />
至少总理当时是非常认真的。他细心倾听，有问必答，一笔一划，认真签名。人和人面对面交谈，装不出来的。<br />
<br />
我为什么忘了向温总理提去年给他写信的事情了？<br />
我的去年关于中国房地产框架8点建议，也是隔海观雾，一些想法而已，不一定对。<br />
地产涉及太广，不是一两句可以说清。我倒是真希望有关部门能够读一下信。<br />
信件当时确实从美国平邮到国务院，给温总理。<br />
上书情结，中国知识分子的特有情结。<br />
<br />
题外话<br />
关于本文的阅读量。<br />
中国的人口真是多。一天内150万阅读，这在美国不可想象。我昨天和华尔街时报的一个朋友讲，他大呼惊讶。<br />
我5月26日在匹兹堡律师协会的演讲，谈中国知识自由和谷歌个案，也要说说这个流量的事情。<br />
<br />
<br />
<br />
几个门户网站的有趣比较<br />
本文在三个主要腾讯，新浪，搜房网站首页新闻，也可以做一个有意思的比较，当然也是片面的了。<br />
腾讯的读者真是多，半天里阅读量就是70万。看来同时腾讯1.5亿人上线实在不是吹牛。<br />
新浪点击少一些，大概是腾讯的三分之一。<br />
搜房是地产网站，政审最严。我的“地产框架建议八条”想重新贴，被暂停候审了，因为“文章涉及到敏感文字”。这篇后记文字居然也被搜房的内容审核系统屏蔽了，真是不知所云。看来没有上市的公司要更加严格地审查自己。<br />
腾讯的反馈比较感性，可能是因为年轻人多些。新浪读者的反馈思考比较多。<br />
对了，新浪的五毛党比例要比腾讯和搜房都多一些。<br />
<br />
问候老朋友<br />
搜房回应的很多是地产界的老朋友。半砖头要我买彩票，我说可以他出钱，我出运气。买彩票，运气和钱一样重要吧。或者是我买了，加收手续费卖给他，当然是在开奖前。<br />
新浪居然是岳老做了沙发。谢谢岳老的问候。<br />
<br />
关于人肉搜索和恐吓邮件<br />
有人人肉搜索我，发来恐吓邮件。我不太理解。<br />
不必人肉搜索我，我的照片，所有历史和办公室联系方式都在博客上。做中美交流的嘛，欢迎垂询。<br />
中国真是大。</div>

							
							
							
							
