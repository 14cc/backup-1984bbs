																											<div id="postmessage_530731" class="t_msgfont">富士康武汉招聘考俯卧撑 男生做不到20个被淘汰<br />
<br />
2010年03月24日14:00<br />
<br />
<img src="http://img.hexun.com/2010-03-24/123095548.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　荆楚网消息 要通过企业招聘,先做俯卧撑过体能关,再穿珠过灵巧关。昨日,富士康鸿准精密模具公司在武汉船舶职业技术学院订单班招聘现场,15名学生被俯卧撑做“趴”。<br />
　　因下雨,原定户外3000米跑步改为俯卧撑。10时58分,通过笔试的112名学生分成8组开始测试。<br />
<br />
　　记者在第三组看到,考官用口令让学生间隔两秒完成一个动作,该组唯一的女生、数控技术专业学生王冲做到第10个时被喊停;一名男生做到15个时就支撑不住;做到20个时,考官称“坚持不住的可以站起来”,当场就有7名学生站起来,其他学生则坚持做完30个俯卧撑。<br />
<br />
　　据该考官称,女生10个、男生20个为合格,男生30个为优秀。据了解,最后有 15名男生因没做到20个俯卧撑被淘汰。<br />
　　随后,剩下的97名应聘者进行蒙眼“画凹槽不规则连续曲线”和三分钟“穿塑料珠”的灵巧性测试,最快的穿了61个、最慢穿了25个,全部通过。<br />
<br />
　　王冲说,做俯卧撑对女生挑战大些,而穿珠对男生的挑战更大。<br />
　　该企业招聘官李女士说,此次招聘的员工将从事模具方面的工作,对体力及精细度的要求都较高。他们的岗前培训将更艰苦:每天早上6点半开始 5000米跑步,外加50个俯卧撑。<br />
　　武汉体育学院运动人体科研所所长张立表示,俯卧撑侧重肌肉耐力测试,对从事车床、搬运等上肢力量为主的工作比较合适。3000米长跑是对心血管功能和耐力的考核,适合长时劳动力的测试,该企业显然是有备而来。<br />
<br />
