													<h2>天朝内斗传说及黄海刺胡传说</h2>
						
						
																											<div id="postmessage_107900" class="t_msgfont">16大上，江zemin以一人半退换来上海帮五大党羽不倒，控制了中央政治局常委中的多数，并不顾全党上下一片骂声，将“三个代表”强行写进党章，实现了上海帮官僚资本主义的理论化，这样，他就不仅确保了自己的政治路线不动摇，而且成功地为自己的历史地位奠基。<br />
2004年，江zemin才假戏真做的全退。但他非但不甘心退出政治舞台，反而下定决心成为中共裆史上第二个太上皇，把曾qinghong扶上国家主席，（“日破云涛万里红”），把黄ju推上总理，把江jinghuan、周yongkang、陈良宇送进政治局常委，将江江zemin时代继承延续下去，发展成江家帮世袭王朝！<br />
2004年3月16日，新华社《内参》报道：朱rongji、李ruihuan、尉jianxing在电视上观看温jiabao于中外记者招待会上答记者问后，给温jiabao 的工作和答中外记者问的评分为“一百分”，称赞温jiabao能正确掌握脉搏，扎实处理尖锐矛盾，是个难得的治国人才。<br />
2004年四月初，陈liangyu在上海市委市政府的会上说：“全国都出现经济过热的倾向，上海的情况就正常，没有过热的问题。经济过热的问题，出在领导同志（温jiabao）身上，就是原则的大问题。”<br />
四月中旬，时任副总理的黄ju借口到上海出席一次国际性会议，瞒着政治局，擅自召开了四省一市（上海市、江苏省、浙江省、江西省、安徽省）党、政、人大、政协四套班子领导人座谈会，秘密串联，煽动东部地方干部批判温jiabao。黄ju在会上表示，欢迎面对面批评黄ju、批评国务院领导的工作，并说：“你们对国务院有意见、有看法，可以以党委、政府的名义给中央政治局、政治局常委会提交报告。”黄ju还发挥说：“有关经济是否过热、超热，党内、业界是有争议的。新一届领导层、新的领导上任，总是想在任内有所作为、有新突破、有新业绩，这是主观愿望，要经实践论证，要看效果。我个人是很担心过热的经济增长。 ”<br />
2004年5月，温jiabao接手国务院的烂摊子，开始宏观调控的孤军奋战。八个政治局常委竟全部作壁上观。温jiabao和江苏、浙江、上海的领导开座谈会，劝他们服从大局，得到的只是表面的敷衍。常州市副市长竟然通过中央电视台公开与总理叫板。<br />
2004年7月1日，南方报系的《二十一世纪经济报道》爆出“股票门”：直指温云松收受马明哲四千万原始股，坐收73．6亿港元。――地方报纸敢于批评国家总理，除了文革，几乎是不可想象的。<br />
2004年7月11日，香港《苹果日报》报道：在最近举行的政治局会议上，陈良宇向影帝发起全面的、正面的攻击。陈良宇在会上指责宏观调控措施已经伤害了江苏、浙江等东部省市，并会在未来几年阻碍全国的经济发展。陈良宇警告温jiabao及其内阁，如果坚持推行调控，必须承担由此引起的伤害经济的“政治责任”。这是自影帝2003年执掌政务以来政治局层级上最直接的冲突。影帝与陈良宇进行了长时间的争论，最后温jiabao声言，一旦经济硬着陆他准备接受批评，并立下军令状，如果宏观调控失败，就引咎辞职。<br />
于是，江家帮在陈良宇的带头冲锋下，疯狂搞面子工程形象工程，准备让经济硬着陆，以毁国为代价逼影帝下台。上海拔地兴建世界第二高的“上海世界金融中心”，各级腐败分子闻风而动，大上快上特大项目，造成当年国民经济损失、内耗、流失近三万亿元。<br />
2005年三月，各省市提出建设各类亚洲国际中心有四十五个；三月以来各地兴建、圈划科技区、保税区、经济开发区、高新技术区、农业试验区、中外合作开发区，又进入了高潮，初步统计有三百七十七个。<br />
北京、上海、广州、深圳、温州等地，房地产、居民住宅价格失控。房地产公司、开发商价格炒作，哄抬房价，非正常牟利。北京、上海在职职工年收入平均为一点六万至一点八五万元，购买普通八十平方米的住宅，要四十年至五十年的收入。当月全国十二个城市提交兴建地铁交通线，总规模有四百二十多公里。<br />
2005年3月30日，在国务院常务会议上，影帝说：今年和今后若干年“首先要加强政府自身的建设与改革”，“特别是领导干部自身改造”，“尊重实际、摆正自己，减少些折腾，减少些内耗，减少些浪费，减少些反覆，减少些自吹自。”影帝在会上立下了军令状：“如果国务院有关决策、措施出问题，而问题又没有及时发现、纠正，或问题的发生产生了严重危害、损失，我影帝要承担，要负责，我会请辞，让贤给其他同志。”<br />
陈良宇如获至宝，立即宣布：上海将建世界最大摩天轮！――总投资超过二十亿元！可同时容纳一千一百多人观光！上海地产、金融股票和银行贷款方面更加失控。为了给楼市降温，影帝亲自去上海做工作，陈良宇豪横地拍了桌子：“党的命令可以听，国务院总理的命令我凭什么听？！”<br />
2005年入夏，江家帮展开了新一轮对影帝的攻势。六月初至七月中旬，上海市委、重庆市委、江西省委、山东省委、浙江省委，先后多次向影帝发难，向中央政治局、中央书记处提出：五中全会应就教育、医疗卫生、社会保障、金融、证券市场、国企等领域改革的挫折、混乱、失败，进行讨论、总结。<br />
陈良宇在上海市委扩大会议上公开指桑骂槐，说：“当务之急的大事，是寻找经金融倒退混乱、改革停滞不前的原因。经验主义、教条主义是主要原因之一。宏观调控搞一刀切，是造成经济波折的原因。”<br />
2005年7月21日下午4点，由温jiabao亲自主持有关人民币升值的紧急国务会议。会议召开之前，宣布了两条纪律：一、会议时间内，一律不准和外界联系，不处理公务；二、会议开至6点，6时正式宣布人民币升值，在此时间内与会者不得离开会议厅。<br />
结果，温jiabao宣布后到对外宣布之前这90分钟空档里，个个与外界联系，致使发 生了228亿美元兑换人民币的事件。这些“人民公仆”在一个半小时之内就净赚了37亿多 元！而且事后连个检讨都没做。<br />
事后影帝听了报告，气得浑身发抖，“有鬼！鬼就在内部。要抓鬼、除鬼，否则国家难有宁日和稳定。”<br />
2005年8月路透社报导，中国总理温jiabao在最近一次中共中央政治局会议中汇报 了中国最新经济动态。但上海市委书记陈良宇在会中猛烈批评宏观调控政策。他指出，这些措施已使江苏、浙江等东部沿海省份遭受损失，未来数年内，这些省份的总体经济发展将会滞缓。影帝和陈良宇二人在政治局会议上进行了长时间的争论，双方都不愿让步。<br />
2005 年8月15日，曾qinghong以中共十六届五中全会筹备小组名义发文件到省部一级党委常委、中央委员、候补中央委员。该文件的内容是：发动各省和中央各部委，要求他们攻击国务院的各种过失，让温jiabao为经济、金融、教育、医疗卫生、社会保障、国企等所有方面的腐败负责辞职。――文件下达后，全党一片震惊，全国一片哗然，至少有三十一个部委办局向中办、国办提出质疑，甚至有人问曾qinghong是否要阴谋政变。<br />
于是政治局被迫下令收回、撤销曾qinghong搞的中共十六届五中全会筹备小组文件。事后，曾qinghong被迫向中央政治局作了书面检查，说自己“党性不强”，把握决策上“严重疏忽”。他承认文件内容和五中全会议题确不一致，未经政治局常委会讨论就转发了。曾qinghong在中央书记处例会上也作了自我批评，承认文件未给大多数成员审阅，在组织原则上是违犯了纪律。<br />
宋平指出，这是严重违反党纪的事件，曾qinghong“人为的要在党内树立个人威信，结果是名声俱失；陈云曾对曾庆红有过批示：‘好表现个人，不宜担任主要领导。’”李ruihuan、尉jianxing也称：“曾qinghong有野心，喜欢搞帮派，从上海到中央都难改。”<br />
2005年11月全国连发三起特大事故，被称作“黑色11月”。11月28日，国务院召开了紧急国务会议，温jiabao在会上作了20多分钟的讲话，20多分钟的发言，在泪流满面中度过，温jiabao说：“心情沉重，不断发生伤亡事件，人的生命就这样丧失了……，松花江水，有毒、污染，还要掩饰人为责任；东风煤矿爆炸是人为责任；江西瑞昌地震伤亡、损害是天灾，也是人为责任。人为责任就是‘人祸’，是对人民、对国家、对事业的罪责。 ”他三次泪洒讲台，哽咽得无法说话，会议因之三次暂停，全场一片沉默。<br />
2006年2月，温jiabao在国务会议和中央政治局会议上都提出了总理问责制。温jiabao在会议上披露了一份关于党政、国家机关干部职务消费情况的调查报告：“2005年公款招待费，三千余亿元；使用、购买公车开支，三千三百余亿元；出国考察、休假开支，二千五百多亿元。用公款在四、五星级宾馆消费，每年更换公车以及由政府、国家事业机构购买进口轿车等等，近年，违规职务消费高达八千亿至九千亿元，这是天文数字，使人心酸的数字，是欠下百姓的数字。这个消耗数，如能正常运作，会使数亿农民解决贫困，肯定会使整个国家面貌发生变化。”<br />
温jiabao提出：“职务消费实际上是公款挥霍、侵吞国家财产，是对全民利益的侵占，是社会深恶痛绝的官场腐败的典型，是社会产生危机的主要因素。”<br />
影帝说：“责任在我身上，问题在体制、机制上。要立法监督，谁违规违法，谁就承担；谁失职渎职，谁就承担。”<br />
影帝说：“总理，作为人民的总理，就得对人民有交待、对国家有交待，对宪法赋予的职责有交待。如果不能解决，不能履行职责，就应该引咎辞职，向人民谢罪。”<br />
2006年5月发生震惊世界的黄海刺胡事件。――胡jingtao视察北海舰队时，两艘军舰突然向胡所在的旗舰开火夹击，当场打死舰上五名战士。吓破了胆的胡jingtao急令旗舰以最高航速逃离演习海域。<br />
随后以“软、迟、怕”蜚声中外的胡jingtao终于派出工作组进驻上海，在监听、威胁下开始调查臭名昭著的社保基金案。<br />
从 6月中旬开始，陈良宇先后在不同场合六次吹风称：“有人要整上海，要搞垮上海，目标是要贬低、否定江总（江zemin），要借反腐败排斥庆红、黄 ju，”“工作组不整出些问题，是不会罢休的。我们思想上、精神上要有准备；”“宏观调控，七成是对着上海的，压上海是明的，排庆红、黄菊双管齐下；”“ 上海市委、市政府有没有问题，谁都不能下结论。”<br />
