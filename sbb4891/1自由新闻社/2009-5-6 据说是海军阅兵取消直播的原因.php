													<h2>据说是海军阅兵取消直播的原因</h2>
						
						
																											<div id="postmessage_88177" class="t_msgfont">在最新一期官方党刊《求是》上海军司令员吴胜利，海军政治委员刘晓江共同署名撰文：坚决抵制军队国家化思想。<br />
<br />
<br />
文中以文革语言表示，不仅表示坚决抵制“军队非党化、非政治化”和“军队国家化”等错误思想的影响，并向胡表忠心，坚决要做到“五个必须”，其前两个必须则是“强化听党指挥，沿党指引前进”和“把科学发展观作为重要指导方针。”<br />
外界分析指出，海军高层急于向胡的表忠心，盖因4月23日胡锦涛在青岛附近海域举行的海军成立60周年海上阅兵，与海军高层发生不愉快，阅兵被迫推迟，海军军头怕遭贬罚，故紧急站队讨好胡。<br />
<br />
胡强行打乱行程<br />
<br />
中共60周年阅兵系列，除北京10月广场阅兵外，还包括了海军和空军的阅兵。海军阅兵被安排在2009年4月23日，在青岛附近的黄海海域。据报导，这是一场22国海军共同构建的海上大阅兵，据称是中共第一次举办多国海军检阅活动，也是中共海军历史上最大规模的海上阅兵。作为军委主席，胡锦涛亲临黄海。<br />
<br />
但是当天海军大阅兵，却异象环生，似乎“出了问题”。原定为23日早上9点开始的阅兵，突然改为下午2点20分，以致媒体都措手不及，所有宣称直播的媒体，都没有能够直播，观众失望，媒体被动。照片显示，胡锦涛阅兵时，竟然出奇的一脸严肃。与在场的军委领导人和海军首脑们，毫无互动。有人发现，胡锦涛一改接见军队代表时，必然身穿“军装”的惯例，第一次着“西装”，检阅海军。胡选用的那条蓝白相间，象征海浪的领带，显然颇费心机。<br />
<br />
对于阅兵时间突然推迟，新华社事后公开解释是，23号上午，胡接见了各国舰长，导致阅兵推迟。外界指出，这样的解释，更显“欲盖弥彰”。胡于22号到青岛后，有足够时间与各国舰长礼节性会见，为何要强行打乱行程，在23号上午动作，临时更改已对外发布的阅兵时间，这只能说发生了“非常事件”。<br />
<br />
胡起疑心<br />
<br />
阅兵前，国防部长梁光烈和海军司令吴胜利都说过：中国不可能永远没有航空母舰。但是吴胜利突然在22号，对举世关注的此次阅兵的最大看点――中国造航空母舰问题，提出了须要进一步听取各方面意见的“说法”。<br />
<br />
显然胡在与军委和海军首脑谈话过程中，对“航空母舰”的最敏感问题，做出了“出乎意外”的表态，海军军头被迫食言。很可能现场火药味浓，并发生了很不愉快场面，导致胡担心被江派人马钻空子，威胁其人身安全。胡有意打破原计划，为临时进行安全检查争取时间。<br />
<br />
黄海教训<br />
<br />
中共十七大召开的前一年，2006年5月初胡曾经历了黄海暗杀未遂事件。当时，胡乘坐中共最先进的一艘导弹驱逐舰到黄海视察北海舰队，突然两艘军舰同时向胡乘坐的导弹驱逐舰开火，打死驱逐舰上五名海军战士。载着胡锦涛的导弹驱逐舰做梦也不敢想有人竟然在光天化日之下谋杀中共最高领导人，于是惊慌失措之下，立即调转头以发疯的速度，载着胡锦涛驶离舰队演习海域，直到安全海域。<br />
<br />
为了避免再次受暗杀，胡换乘舰上的直升飞机飞回青岛基地。未敢作停留，也未回北京，直飞云南。一个星期之后，胡把一切安排妥当，才回北京露面，胡锦涛行程十日之前对外是空白，新华社发消息十一日至十五日胡在云南考察。<br />
<br />
2006年，海军司令张定发病发去世时，官方既没有吊唁，也没有悼词，新华社、解放军报都未报导，只有海军自己的小报儿《人民海军报》2006年12月17日在头版刊出个简讯：“中央军委委员、海军原司令张定发同志，因病于十二月十四日在北京逝世，享年六十三岁”，消息中只有一个简单的不能再简单的履历，甚至连个黑白遗照都免了。张定发是江的死党，外界怀疑其与黄海暗杀有关。<br />
<br />
海军军头发出左转信号，模仿2009年04月01日中央军委委员、总政治部主任李继耐，在《求是》杂志发文表示“坚决抵制军队国家化等思想”的作法向胡表忠心，以期胡手下留情，放过一马。<br />
<br />
据悉，胡高度重视六十周年的阅兵，以期载入历史，而像海军阅兵的“意外事件”，很可能遭至严厉秋后算帐。<br />
