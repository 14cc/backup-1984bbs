													<h2>关于公盟税案所涉庄璐责任的意见</h2>
						
						
																											<div id="postmessage_237912" class="t_msgfont">郭玉闪：关于公盟税案所涉庄璐责任的事实与意见<br />
<a href="http://www.chuwangtai.cn/766.html" target="_blank">http://www.chuwangtai.cn/766.html</a><br />
本文出自楚望台的法律评论（ChuWangtai.cn）<br />
<br />
　　公盟税务事件发生以来，过程一波三折，所幸于公盟主要负责人许志永君，在海内外舆论强力声援之下，历二十数日牢狱之灾，终得取保候审，脱厄而出。唯公盟出纳庄璐女士，无辜被拘二十多日，获释后为避祸返乡，风波犹不能平。志永数次公开发言，声称为庄璐担当计而狱中认罪，言下之意不仅公盟有罪，且罪在庄璐。以庄璐之无辜，受如此之责难，令人震惊；此中细节，不得不辨。<br />
<br />
　　依实情论，公盟并无偷税罪，但有管理失察之责。公盟无人懂财会，为此特外聘春雪会计公司做账，具体系一刘姓会计负责为公盟入账。公盟所漏登之两笔耶鲁大学资助即其手笔，情无可饶。依会计操作规范，即便曾经漏登数笔，至年终也需核对其所做余额与银行对账单余额，若不符，则会计有检查义务；如系出纳漏交一些凭证所致，也可籍年底核查检出并修正之。显而易见，公盟在税务上的疏漏，此刘姓会计需负主责，公盟虽有管理失职之嫌，但绝无主观故意。此间，庄璐仅为出纳，既非志永所称“财务主管人员”，亦非志永所称“直接责任人”。此不得不辨之一。<br />
<br />
　　庄璐不仅无罪，且有功于公盟。当地税突击检查之后，作为公盟七人决策委员会成员之一，在得知公盟可能有数笔收入未被会计入账后，我即致电提醒志永立即联系会计公司交涉核实此事，志永却未予处置，反于同日关机赴京郊静养。在法人代表缺位而税务又咄咄逼人之际，出于对公盟的关心，我致电要庄璐与会计公司核实，如确有错误，及时修正。后查证属实。对志永作为公盟总负责人在税务问题上应对失责之处，我在隔日公盟例会上曾对志永当面批评，并向例会全体人员告知此事。庄璐与会计公司所作之核实，为企业正常自查范围，本应由公盟总负责人志永出面交涉，若庄璐为心机深沉之辈，在法人代表撒手不管之际定会推脱。然庄璐心思单纯，勇于承担，修正会计公司错漏之误，后公盟据此将相应税款补齐。凭心而论，此为公盟后得以抗辩税务处罚之根本；若无庄璐之所为，公盟将毫无招架之力。此不得不辨之二。<br />
<br />
　　公盟事件本非简单税案，明眼人都知此乃欲加之罪。庄璐仅一单纯女孩，只因就职于公盟而无辜受累，在看守所受罪二十多日。公盟诸君，于情于理，都应善加爱护庄璐；然事件发生以来，却对其时有苛责。庄父赴京为女儿办理各项法律手续，公盟办公室数位工作人员横加指责，数落庄璐“牵连”公盟之“罪状”，庄父心焦女儿状况之外还要承受如此谬责，情何以堪！此为公盟对庄璐不妥之一。志永在认罪之际将庄璐也牵涉进来，称庄璐受其指示将几笔赞助做不入账处理。入账本为会计职责，何来出纳入账之说？庄璐与此毫无瓜葛，遑论负单位犯罪之责；但若依志永所称，则庄璐为共犯无疑，在法律上可真正获罪，正与志永所自称欲保护庄璐之认罪动机南辕北辙，此为不妥之二。最不妥之处为志永出看守所后所作文章，甚令人不解：若如其所言，其为庄璐担当，则其心目之中，是否已确认公盟有罪？且罪不在他而在庄璐？如其确信庄璐有罪且愿为其担当，为何又昭告天下、自翻其供？既然如此，何可复言其为庄璐担当？凡此种种，令人遐想，坊间已是众论纷纭。事涉庄璐清白，志永及公盟需向公众清楚解释，如并不认为庄璐有罪，则需为其不妥之言论向庄璐道歉，如确信公盟有罪且罪在庄璐，则其需以事实道明公盟及庄璐何以有罪。此不得不辨之三。<br />
<br />
　　公盟从事法律援助多年，明枪暗箭，在所难防。<strong>社会之进步也非坦途；值此明暗之际，最需明辨是非。无论公盟遭逢如何，都应平心静气、择善而从。公民社会之所恃，唯公义与担当而已。若是非不明，则公盟虽存犹亡，若推诿塞责，则难免欺世盗名之讥。此乃公民社会大局大义之所在，也为志永及公盟诸君立身之根本所在。人无信不立，事匪义难行，望诸君善加体察。</strong><br />
<br />
 <br />
<br />
许志永《感恩每一个人》<br />
<br />
　　有朋友问，这次事件最深刻的感受是什么，我说是感动，在看守所内心充满感动，出来以后知道那么多关心和支持，更加感动。<br />
<br />
　　要感谢的朋友太多了！抱歉因为电脑和网络的原因直到今天才来得及上网说一声谢谢。陈敏、萧瀚、张亚东、毛向辉、赵连海、北风、王荔蕻、金怀鱼等一大群朋友们组织声援，江平、茅于轼等前辈们站出来呼吁，单亚娟等访民们执着地为我们奔走，昔音等爱心环保公益团队为我们呐喊，成千上万素不相识的网友们、媒体的朋友们、法律界朋友们、香港同胞、台湾同胞关心和声援，耶鲁大学法学院葛维宝教授努力争取，特别值得一提的是我的同仁们――公盟成员在黎雄兵和王功权等人的带领下承受了巨大压力坚持理性维护权利。要感谢的朋友太多了，这个名单应该很长很长，我想说，感谢大家关心和支持！我很幸运生活在这样一个进步的年代！<br />
<br />
　　其实我知道，朋友们在外面承受的压力比我大的多。在看守所，我平静地接受命运的安排，除了提审谈税务问题外主要思考一些宗教和哲学方面的问题，也反思自己曾经的过错。最担心的是庄璐，如果定了罪真不知道她以后的生活会怎样，我希望她能在逮捕之前获得自由，所以承担了所有指控的责任。我承认了耶鲁大学法学院的几笔赞助没有记账（进看守所之前我一直以为记账了）是因为我指示庄璐不记账，也承认根据修订前的刑法 201条公盟构成了偷税罪（我要求看刑法第七修正案他们给我看的是修订前的第201条），尽管公盟从事非营利公益事业，但既然注册为公司，有关部门要求我们按照现行法律纳税是有道理的，毕竟公盟存在重大过错导致没能及时缴纳一些税款。我也为自己曾经的激烈言辞而后悔，我们遵守现行法律，也不会停止对社会进步的探索和呼吁。<br />
<br />
　　无论发生了什么，我们会继续遵循法治、理性的原则推动社会进步。也希望公盟税案能引起社会广泛反思，一方面，工商注册的民间公益机构应该在税收方面更严格要求自己，另一方面，希望政府有关部门开放民间公益组织注册，推动社会开放。感谢大家的关心和支持！<br />
<br />
