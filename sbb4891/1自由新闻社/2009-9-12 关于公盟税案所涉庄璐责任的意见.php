　　最近在思考一些宗教和哲学方面的问题，所以很抱歉时常关机一些朋友打电话找不到我。但是，有些事情不得不处理，所以基本上都安排在周末。今天早上和葛维宝教授聊天，过去几天，他们和刘延东、杨洁篪等人谈耶鲁大学与中国有关方面的学术研究合作计划。下午和朋友讨论巴哈伊。晚上与临沂市平邑县政法委官员谈姚晶的事情，出于对姚晶长远负责考虑，我其实愿意站出来从中调解。<br />
<br />
　　然后，蒙蒙细雨中，与上海来的朋友们喝酒聊天。似乎总会涉及看守所的事情。我发现警方总能抓住每个人的弱点，把庄璐抓进来就是我的软肋。从单位犯罪的法律讲，如果人家非要给公盟定罪，而我作为法定代表人又确实不知情，庄璐无论作为财务主管人员还是直接责任人一定会作为替罪羊。我太懒确实记不得财务的事情，为了让庄璐尽快出去，承认了一些根本不知道的（也许是忘了）事情，后来，也是由于希望庄璐尽快出去，认可了“偷税罪”（其实现行刑法已经没有此罪名）。记得最后一天我对预审说，我能为庄璐做的只能这些了，要怎么判随你们吧。记得8月13号那天下午审讯室外面突然见到了庄璐，我喊庄璐的名字，她木然看着我没有反应，后来觉得，这也许是他们有意安排的，好让我相信庄璐已经非常痛苦。还好，根据法律，现在案件应该终结了。<br />
<br />
　　然后谈新疆问题。这是一个时代的悖论。社会稳定有两种基本模式，一种是极权专制的稳定，靠大规模杀人带来的恐怖能够维持稳定，一种是现代民主法治公平正义基础上的和谐稳定。这个时代，王震模式的稳定已经不可能了，中国正在融入文明的世界。而官员们还缺少能力制造民主法治公平正义的稳定，所以我们对新疆的未来充满忧虑。想到了2006年在新疆，那时心中已经充满忧虑。那些贫困让人难过，那些愤怒让人担忧，晚上夜市武警荷枪实弹维护的大巴扎的稳定让人揪心。也许，在保守势力看来，新疆只有采取铁血政策才能维护统一，可是，这已经是一个文明的时代。而我们的一些政治理念――比如不能允许真正的自治―― 几乎阻止了协商的可能。未来该怎样，我们真的忧虑。<br />
<br />
　　我们的社会似乎分歧太大了，这群忧虑的中国人可能被另一群人视为一个民族的叛徒，甚至，我们所有消除分歧的努力也被视为背叛，这是一个民族的悲剧。而我们，依然执着地努力化解这可能的悲剧。<br />
<br />
