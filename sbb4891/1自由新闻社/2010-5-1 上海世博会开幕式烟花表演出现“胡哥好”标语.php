世博轴在夜色中亮灯，变幻出许多不同的光影造型。其中还显示出“胡哥好”的字样。图片来源:东方IC<br />
<br />
中国网5月1日报道 昨天晚上8点10分，中国2010上海世博会开幕式正式举行。本报记者在上海世博园世博文化中心亲历了这一盛事。在国家主席胡锦涛宣布世博会正式开始后，开幕式文艺表演随后开始。<br />
<br />
毛阿敏、刘媛媛、周华健联袂演唱了《相约上海》，表达了上海对世界游客的欢迎。 演出第二部分《江河情缘》则表现了西方的多瑙河和东方的长江的精彩对话。<br />
<br />
舞台上一片星空闪烁，随着《茉莉花》的轻柔音乐，大型交响乐团出现在沉浸于碧波荡漾的舞台上，奏出在1867年巴黎世博会之际创作的《蓝色多瑙河》，随着音乐的起伏，神奇的小球矩阵忽而幻化成立体的EXPO字样，然后渐渐变成“中国馆”的造型。此后，著名钢琴家郎朗在舞台上激情演奏钢琴协奏曲《新上海协奏曲》，他激情四溢的演奏引起现场一片惊叹。<br />
<br />
随后，中国2010年上海世博会海外推广形象大使、著名歌手谷村新司为全场带来了耳熟能详的经典歌曲《星》，而就在舞台变幻出夜空星光璀璨的场景时，台下的观众也纷纷打开手中的手电，于是一片灿烂的星海在观众席上显现，和着动人的曲调，此起彼伏。<br />
<br />
现场表演和观众的互动成为昨天演出的最大亮点之一。在另一曲《致世博》歌曲演唱时，观众还手拉手做出一个“心”的形状，全场激荡出一片感人的氛围。<br />
<br />
最后，江巴才仁等两名青海玉树的地震孤儿出现在舞台上，他们天真的笑容让在场的所有观众不禁动容。热烈的掌声中，舞台背后的大屏幕上打出了“心手相连、大爱无疆”的字样，有观众的眼角已经湿润。<br />
<br />
灿烂烟花：一场水与火的魔术<br />
<br />
文艺演出之后，记者转场前往黄浦江边欣赏焰火表演。江风吹来，微微有些凉意。突然，人群一阵惊呼。上千盏探照灯和数十盏激光灯哗地打开。世博园区边上南浦大桥和卢浦大桥同时亮灯，一瞬间，上海黄浦江两岸动人的夜色被清晰地勾勒在观众眼前。<br />
<br />
而记者身后，壮观的世博轴也已经在夜色中亮灯，而且变幻出许多不同的光影造型。其中还显示出“胡哥好”的字样，让看到的游客会心一笑。<br />
<br />
<br />
同时，一块283米宽的巨大LED屏幕出现在黄浦江上，和着音乐和焰火表演呈现出动人的画面，在场的不少游客一片惊叹。有一些坐着轮椅的参观者也不禁和着音乐不断舞动自己的双手。<br />
<br />
随后，灿烂的烟花和律动的喷泉表演和谐地融合在一起，随着音乐的起伏，天空中火树银花，黄浦江上喷泉涌动。漫天的焰火中，有微微的水滴被江风吹到了游客的身上。而美妙的《茉莉花》的曲调中，一场水与火魔术进入高潮。黄浦江对岸的灿烂灯火和变幻的光影的营造出一片有如梦幻的场景。<br />
<br />
直到焰火晚会结束，很多游客依然恋恋不舍地不断回头，朝着江边露出赞叹的笑容。<br />
