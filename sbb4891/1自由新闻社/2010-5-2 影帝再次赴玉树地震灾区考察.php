																											<div id="postmessage_594387" class="t_msgfont">影帝再赴青海玉树地震灾区考察&nbsp; &nbsp;&nbsp; &nbsp;<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;中央人民政府网站5月2日报道（老子注，下同：不去SB会，非要去玉树，新华社都不肯报道了，只好用自己的政府网，傻逼了吧）5月1日至2日（SB会刚刚开幕的时候，全国人民都沉浸在喜悦的气氛中时，丫要特立独行），中共中央政治局常委、国务院总理温家宝再次赴青海玉树地震灾区，深入村庄、学校、“方舱”医院（“方舱”医院？是不是出院的人还要在出门儿的时候拽兮兮的说“我已出舱，感觉良好”？）、自来水厂和寺庙等，看望慰问灾区各族干部群众，了解灾后群众安置情况，指导灾区恢复重建工作。正值“五一”国际劳动节，温家宝代表党中央、国务院向战斗在抗震救灾第一线的广大干部群众、解放军指战员和武警、公安消防官兵表示亲切慰问。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;4月14日，玉树发生7.1级强烈地震。玉树州所在地结古镇住房和基础设施损毁严重，2220多人遇难，70人失踪。地震发生后第二天，正是抢险救灾的关键时刻，温家宝赶赴玉树灾区指导抗震救灾工作（关键时刻还给别人添乱），明确要求只要有一丝希望，就要尽百倍努力，争分夺秒抢救废墟下的生命。这以后，温总理每天不断关注抗震救灾情况，多次作出重要指示。经过多方抢险救援人员半个多月的不懈努力，人员搜救已经结束，伤员全部得到救治，受灾群众的吃饭、临时住所、医疗有了基本保障，通讯、供水、供电等基本恢复，部分学校复课，废墟清理开始进行，玉树抗震救灾工作转入恢复重建的新阶段。在这又一关键时刻，温总理再次来到玉树，看望慰问灾区群众，实地了解救灾情况，部署恢复重建工作（扫把星又来了）。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;1日上午11时许（瞧这时间挑的，三条光棍儿，难怪出国不带老婆），飞机一起飞，温家宝就和中共中央政治局委员、国务院副总理、国务院抗震救灾总指挥部总指挥回良玉一起，召集随行的国务院有关部门和部队有关负责人，开会研究玉树灾后重建的方针政策（坐飞机的时候才开会，坐飞机之前去干嘛了？）。这次空中办公会开了一个半小时（斗了一个半钟的地主)。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;下午2时许，温家宝一下飞机就赶赴设在结古镇赛马场的北京军区某部“方舱”医院。温家宝走进帐蓬医院、门诊室、B超室、重症急救室、X线室和保障供应室，详细察看医疗保障和卫生服务情况。截至29日，这里已接诊收治病人1290人，受到灾区群众欢迎。玉树县甘达村党支部书记叶青正在帐蓬医院里接受治疗。大地震中叶青3根肋骨折断，他的妻子也失去生命，但他强忍悲伤和病痛折磨，带领村民们奋起抗震救灾。因病情不断加重，才于近日住院治疗。温家宝说，基层干部都应向你学习（这还要学习，连命都不要的人。。。）。希望你安心养病、早日康复。68岁的藏族女农民索南拉毛看到总理走近她的病床，激动地哭起来。地震中，她的腿部被砸伤，目前正在逐步康复。温家宝握着她的手安慰道：“不要着急，用不了多久，你就可以像以前一样走路了（万一她以前就不能走路呢？）。”门诊室每一个诊台旁，都有一名军医和一名藏族医生共同为群众诊疗。温总理叮嘱他们要为老乡看好病。在手术室里，当听说这里灾后已经剖腹产诞生了两个新生命时，温家宝十分高兴。他对部队医务工作者说，你们不远千里来到地震灾区，救死扶伤，为受灾群众服务，展现了革命的英雄主义气概和救死扶伤的人道主义精神，不愧为人民的子弟兵，灾区人民感谢你们，党和政府感谢你们！（这句陈词老调说了多少遍了，编剧也不知道换一句台词）<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;随后，温家宝又驱车前往地处结古镇西南方向70多公里的隆宝镇。汽车沿着崎岖不平的山路盘行，途中要经过海拔4600多米的红土山垭口，这里空气含氧量仅为平原地区的50%。汽车经过垭口公路最高端的时候，温家宝看到有正在作业的养路工人，他临时停车走过去向他们致以“五一节”的问候。（应该是致以世博会的问候！）<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;近2个小时的山路颠簸，温家宝来到海拔4300米的隆宝镇。隆宝镇是玉树大地震的震中，全镇6个村21个社受灾严重，所有房屋严重损毁，102人死亡。温总理弯腰走进君青村帐蓬党支部，详细了解人员伤亡、房屋毁损以及群众安置情况。随后，他又来到帐蓬医疗点，看望受伤群众，38岁的桑丁在地震中失去了丈夫和两个孩子，她本人腿部骨折，头部也受了伤，正在帐蓬医疗点治疗。78岁的父亲旦巴和一个四岁的孩子陪她。听了这个家庭的不幸遭遇，温家宝一手握着她的手，一手握着旦巴的手说：“你们失去了亲人，我的内心和你们一样难过。困难总会过去的，党和政府会帮助你们把生活安排好，把家园建设好（疑似以前用过的台词）。”他还来到杂年村受灾群众集中安置点，看望群众和医务人员。温家宝对聚拢来的村民们说：“地震夺走了我们的亲人，但夺不走我们的意志。灾难会过去的，但我们从灾难中得到的进步永远不会过去。玉树不倒，玉树常青；人间有情，大爱无疆。在党和政府的支持下，在全国人民的关心下，在灾区人民的努力下，我们一定能够重建美好家园。祝大家‘扎西德勒’（明年春晚会有一个叫《党的政策扎西得勒》的节目）！”听了总理坚毅而深情的话语，人群中爆发出热烈掌声。一些藏族群众流下了眼泪（汉族群众早就听烦了，所以没有流泪）。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;玉树州有84座寺院不同程度受损，其中结古镇受损寺院8座。有600多年历史的结古寺，坐落在结古镇以西两公里的彭措达泽山上，远远望去，从山顶到山腰，一片绛红色的建筑独具风格，十分引人注目。结古寺是玉树地区藏传佛教影响最大的寺院之一，九世班禅曾在这里圆寂。地震中8名僧人遇难，寺庙建筑损毁严重。听说温总理要来，前往寺院的山路两旁站满了僧众，他们手捧哈达献给总理。温家宝走进桑周加措大经堂，听取更嘎松保堪布的介绍，认真察看了寺庙损毁程度，询问文物抢救进展情况。他亲切地对更嘎松保说，党和政府会尽全力帮助你们恢复重建毁坏的寺庙，抢救和保护好重点文物。更嘎松保十分感动，他感谢党和政府的关心（先感谢国家！），表示将和僧众一道，积极投身玉树灾后重建工作。温家宝对地震中遇难的僧侣表示哀悼。他说，大灾面前，广大僧侣发扬爱国、爱教、爱家乡的精神（怎么不让他们爱党？），在抢救人员、安抚群众、维护社会正常秩序方面做了大量工作。我代表党中央、国务院向你们表示慰问和感谢。只要各族人民和僧俗群众团结互助，共同奋斗，就一定能够建设一个更加美丽的新玉树，让广大人民群众生活得更美好（又是以前的老词儿）。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;晚9时，温家宝在青海省抗震救灾指挥部帐篷会议室主持召开会议，研究玉树灾后重建工作并作重要讲话。散会后已是23时30分左右。他又步行来到设在玉树州体育场的济南军区某部“方舱”医院，看望伤员、慰问医务工作者（那天晚上十二点之后的事，我们谁也不告诉）。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;温家宝一直牵挂着地震灾区孩子们的生活和学习情况。2日一早天刚放亮，他从住宿的帐篷出来，来到了玉树县第三完全小学。这个小学有3130名学生，地震造成91名学生、5名教师遇难，两栋教学楼已成危房，其余校舍全部倒塌。在各方面大力支持下，四至六年级学生已于4月24日复课。在六年级一班活动板房里，同学们正在学习课文《跨越百年的美丽》。走进教室，温家宝亲切地对同学们说：“哪位同学愿意给爷爷唱首歌（以演技著称的影帝考察文艺工作）？”13岁的女孩永芷拉毛勇敢地从座位上站了起来，动情地唱起歌曲《感恩的心》（应该唱《你好毒》），在她的带领下，全班同学齐声合唱。总理静静地听完后说：“这首歌的歌词非常好，让我们记住其中的一句话‘人间坎坷辛苦，我不认输’（影帝应该在此时清唱一曲《爱拼才会赢》）。”走出教室，温家宝手持一束白菊，缓步来到学校的废墟前三鞠躬，悼念在大地震遇难的同胞们（温家宝俯身献菊花）。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;玉树州综合职业学校在地震中受到严重损失，学生们失去了上课的教室。到4月29日，365名学生已转移到青海省内有关学校，高三年级的132名学生全部复课。离高考只有36天了，孩子们正在帐篷学校里紧张复习功课。温家宝走进高三(1)班了解了孩子们的学习情况，对他们说：在你们人生新的起点即将到来的时候，遇到了一场灾难，这是终身难忘的一课。通过这场磨难，你们的意志会更坚强，像玉树一样不屈不挠、挺立不倒（全国都地震一次的话那年轻人岂不是都坚强了？）。希望你们以百倍的努力刻苦学习，将来为祖国、为家乡的建设做出贡献。你们就是我们的孩子（我也有孩子了！），我会惦念你们的。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;走出教室，闻讯而来的师生们围拢到总理的身旁，学生大多数是藏族。温家宝深情对他们说，中华民族5000年的历史，就是灾难深重的历史，但历经磨难，各民族文化传承不断，各民族依然团结得像一家人，这是我们这个民族大家庭的特点，也是我们战胜困难的力量源泉。灾难可以化作民族进步的力量，我们要用自己的双手创造玉树更加美好的未来（又是多难兴邦的老调子，意思就是说民族的进步要靠灾难来推动）。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;地震发生后，玉树自来水管网系统等遭到严重损毁。在青海省水利厅努力下，已有10多家单位和灾民安置点通水（十多家也好意思写出来）。温家宝来到玉树电力集团公司自来水厂，详细询问供水供电设施恢复情况。他勉励职工们再接再厉，保证地震灾区的供电和群众的用水。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;临离开结古镇时，玉树灾区的广大群众在街头与总理依依不舍地告别。凝望着山坡上的一片片废墟，温家宝深情地说：“玉树，我还会再来的！”（全文的点睛之笔，我胡汉三还会再回来的！）<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 综上所述，就是王小波的那篇文章《一只特立独行的猪》~</div>

							
							
							
							
