													<h2>温家宝在青海玉树赞扬僧侣爱国精神</h2>
						
						
																											<div id="postmessage_594681" class="t_msgfont">温家宝引用歌词鼓励玉树学生 赞扬僧侣爱国精神<br />
<a href="http://www.sina.com.cn" target="_blank">http://www.sina.com.cn</a>&nbsp;&nbsp;2010年05月03日01:54&nbsp;&nbsp;新京报<br />
<br />
　　据新华社电 中共中央政治局常委、国务院总理温家宝1日晚在青海玉树主持召开会议研究灾后恢复重建工作。他强调，玉树地震灾区抢险救灾、应急救援基本结束，进入全力医治伤员、安置受灾群众，恢复正常秩序、加快恢复重建的新阶段。<br />
<br />
<br />
　　做好灾区心理抚慰工作<br />
<br />
　　温家宝强调，今后的抗震救灾工作重点放在以下九个方面：<br />
<br />
　　一是进一步安置好受灾群众生活。全面摸查，确保每一位受灾群众都能有基本生活保障。二是切实加强灾区卫生防疫工作。确保卫生防疫覆盖所有受灾乡、村和集中安置点。三是全力保障基础设施恢复正常运行。<br />
<br />
　　四是尽快建立正常的生产生活秩序。维护市场和物价基本稳定。切实做好灾区干部群众的心理抚慰和公共安全工作，增强灾区群众的安全感。<br />
<br />
　　倒屋重建采取补助措施<br />
<br />
　　五是做好灾害评估工作。重点评估人员伤亡失踪人数，灾害范围，基础设施、房屋毁损情况。<br />
<br />
　　六是抓紧编制灾后恢复重建规划工作。尽快编制灾后恢复重建总体规划和专项规划。<br />
<br />
　　七是研究制定支持灾后重建的政策措施。考虑玉树和青海的特殊困难，恢复重建所需资金主要由中央财政资金安排，同时采取更加优惠的税收、金融、土地、倒屋重建补助等政策措施。<br />
<br />
　　八是科学组织实施灾后恢复重建工作。基础设施要优先安排学校、医院等公共服务设施的恢复重建，严格执行强制性的建设标准规范，确保安全。九是促进民族团结进步事业发展。<br />
<br />
　　■ 特写 总理废墟前三鞠躬悼遇难同胞<br />
<br />
　　温家宝再次赴玉树考察，看望慰问灾区各族干部群众<br />
<br />
　　总理废墟前三鞠躬悼遇难同胞<br />
<br />
　　5月1日至2日，温家宝再次赴青海玉树地震灾区，深入村庄、学校、“方舱”医院、自来水厂和寺庙等，看望慰问灾区各族干部群众，了解灾后群众安置情况，指导灾区恢复重建工作。正值“五一”国际劳动节，温家宝代表党中央、国务院向战斗在抗震救灾第一线的广大干部群众、解放军指战员和武警、公安消防官兵表示亲切慰问。<br />
<br />
　　飞机上 空中办公研究重建<br />
<br />
　　4月14日，玉树发生7.1级强烈地震。地震发生后第二天，温家宝赶赴玉树灾区指导抗震救灾工作。<br />
<br />
　　5月1日上午11时许，飞机一起飞，温家宝就和中共中央政治局委员、国务院副总理、国务院抗震救灾总指挥部总指挥回良玉一起，召集随行的国务院有关部门和部队有关负责人，开会研究玉树灾后重建的方针政策。这次空中办公会开了一个半小时。<br />
<br />
　　方舱医院 安慰受伤藏族群众<br />
<br />
　　下午2时许，温家宝一下飞机就赶赴设在结古镇赛马场的北京军区某部“方舱”医院。68岁的藏族女农民索南拉毛看到总理走近她的病床，激动地哭起来。地震中，她的腿部被砸伤，目前正康复。温家宝握着她的手安慰道：“不要着急，用不了多久，你就可以像以前一样走路了。”门诊室每一个诊台旁，都有一名军医和一名藏族医生共同为群众诊疗。温总理叮嘱他们要为老乡看好病。<br />
<br />
　　结古寺 赞扬僧侣爱国精神<br />
<br />
　　玉树州有84座寺院不同程度受损，其中结古镇受损寺院8座。有600多年历史的结古寺，地震中8名僧人遇难，寺庙建筑损毁严重。温家宝走进桑周加措大经堂，听取更嘎松保堪布的介绍，认真察看了寺庙损毁程度，询问文物抢救进展情况。他亲切地对更嘎松保说，党和政府会尽全力帮助你们恢复重建毁坏的寺庙，抢救和保护好重点文物。温家宝对地震中遇难的僧侣表示哀悼。他说，大灾面前，广大僧侣发扬爱国、爱教、爱家乡的精神，在抢救人员、安抚群众、维护社会正常秩序方面做了大量工作。<br />
<br />
　　板房教室 引用歌词鼓励学生<br />
<br />
　　2日一早，温家宝从住宿的帐篷出来，来到了玉树县第三完全小学。<br />
<br />
　　这个小学有3130名学生，地震造成91名学生、5名教师遇难，两栋教学楼已成危房，其余校舍全部倒塌。四至六年级学生已于4月24日复课。在六年级一班活动板房里，同学们正在学习课文《跨越百年的美丽》。走进教室，温家宝亲切地对同学们说：“哪位同学愿意给爷爷唱首歌？”13岁的女孩永芷拉毛勇敢地站了起来，唱起歌曲《感恩的心》，在她的带领下，全班同学齐声合唱。总理静静地听完后说：“这首歌的歌词非常好，让我们记住其中的一句话‘人间坎坷辛苦，我不认输’。”<br />
<br />
　　走出教室，温家宝手持一束白菊，缓步来到学校的废墟前三鞠躬，悼念在大地震遇难的同胞们。据新华社电</div>

							
							
							
							
