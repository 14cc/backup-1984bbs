<br />
作者：田纪云<br />
来源：《炎黄春秋》2010年第6期<br />
本站发布时间：2010-6-11 8:18:52<br />
<br />
从1941年入伍至2003年退休，我从政六十二年，期间，在地方工作四十年，在中央机关工作二十二年。在我长期从政同各种人物打交道或相处中，使我深深体会到作为一个领导干部，特别是高级领导干部，起码应具备以下政治品德：<br />
<br />
要敢于坚持原则。这是作为一个高级领导干部最基本的素质。在关键时刻，在大是大非的问题上，要敢于说真话，敢于坚持党的原则，不能做墙头上的草，刮风两边倒。毛主席说要“五不怕”，我看有“一不怕”就行，就是不怕丢乌纱帽。没有原则性的人，老好人，噙着冰化不出水来的人，或者只会鹦鹉学舌的人，是不具备当领导干部的条件的。当一个错误潮流来临，个人又无力抗拒时，也要尽量避免卷入，不说或少说违心的话，不做违心违纪的事。例如“文化大革命”，谁也抗拒不了，但你至少可以不去诬陷他人，更不要去打人。<br />
<br />
要处以公心，秉公办事。这对领导干部来说是至关重要的品德。<br />
<br />
主要体现在用人、容人和团结人三点上。<br />
<br />
所谓用人，就是要任人唯贤，不能任人唯亲，要搞五湖四海，不搞小圈子。用人不能受什么老部下、老同事、老乡亲、三亲六故等因素的影响，更不能以人划线，拉帮结派。古人说，“举贤不避亲”，不能倒过来，变成“举亲不避嫌”。在用人问题上，要力避有恩必报、有仇必报或者恩仇都报的江湖义气，这是党性原则所不允许的。用人问题是衡量一个领导干部素质的最重要的标志之一，也是我们党保持先进性和优良传统的关键所在。否则，你说得再多、再好听也没人相信。<br />
<br />
所谓容人，就是要谦虚谨慎，博采众长。要心胸开阔，能够听取不同意见。俗话说，“兼听则明，偏听则暗”，“宰相肚里能撑船”，就是说作为领导干部气度要大一些，善于集思广益。凡是领导干部听不得不同意见的，刚愎自用，一触即跳，动不动就训人骂人，让人望而生畏者，难免决策失误，给党和国家造成损失。毛主席说：三个臭皮匠，赛过诸葛亮；一个篱笆三个桩，一个好汉三个帮嘛！要懂得多数人的智慧总比一个人高，个人与群众相比是微不足道的，永远是人民的小学生。“谦受益，满招损”，这是自古不变的真理。<br />
<br />
所谓团结人，就是要宽以待人，团结各方面的人，包括有不同意见的人；不要求全责备。谁有点不同意见，就记仇，伺机报复，给小鞋穿，这是心胸狭隘，心术不正的表现，缺乏领导干部应有的水平，更非政治家的表现。<br />
<br />
真正深入实际，走群众路线，贯彻民主集中制原则。从群众中来，到群众中去，这是毛主席一贯倡导的工作方法。凡重大问题要提请集体讨论，让每个领导成员充分发表意见，不可搞“一言堂”，“家长制”，个人说了算。胡耀邦同志任总书记时，是我们党历史上最民主的时期之一，人们敢讲话，讲真话，不担心抓辫子、戴帽子、打棍子。<br />
<br />
要激流勇退，不要恋权恋位。党的十一届三中全会后，小平同志大力提倡干部的年轻化，提出废除事实上的干部终身制。胡耀邦同志身体力行，在推动干部年轻化方面做出了卓越贡献，各级都提拔了一批年轻干部进入领导班子。胡耀邦同志多次说过，部长干到65岁，中央领导干到70岁就可以了，他自己只准备干到70岁。任期满了，或者年龄到了就退下来，天经地义，不胜光荣。现在人才辈出，不愁后继乏人。要明白，到了眼睛睁不开了，嘴巴合不拢了，腰也直不起了，头脑也不清醒了，还赖在台上，是不讨人喜欢的。<br />
<br />
退休，关键是要“休”。领导干部退休，关键是要“休”。退休后应安心修身养性，颐养天年，争取多活几年，享享天伦之乐。千万不可再利用各种关系干预“朝政”，对现在的领导人指指点点。要知道，革命就像接力赛跑一样，一人跑一棒，你的一棒跑完了，就坐到一边休息去吧，不要再对跑下一棒的人指手划脚。更应懂得，长江后浪逐前浪，世上新人换旧人。地球离了谁都照样转，而且转得更好！<br />
<br />
这些体会中没有涉及利用特权榨取人民血汗的腐败分子，因为那是另类性质的矛盾。</div>

							
							
							
							
