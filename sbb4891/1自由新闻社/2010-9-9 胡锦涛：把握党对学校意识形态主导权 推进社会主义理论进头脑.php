一、充分认识推进教育改革和发展的重大意义<br />
<br />
教育是民族振兴、社会进步的基石，是提高国民素质、促进人的全面发展的根本途径，寄托着亿万家庭对美好生活的期盼。强国必先强教。新中国成立以来，党和国家始终高度重视教育。经过60多年特别是改革开放30多年的不懈努力，在我国这样一个拥有13亿人口的发展中大国，我们开辟了中国特色社会主义教育发展道路，建成了世界最大规模的教育体系，保障了亿万人民群众受教育的权利，城乡免费义务教育全面实现，职业教育快速发展，高等教育进入大众化阶段，教育公平迈出重大步伐，办学水平不断提高，取得了举世瞩目的伟大成就。我国教育事业不断发展，极大提高了全民族素质，有力推进了科技创新和文化繁荣，推动我国实现了从人口大国向人力资源大国的转变，为我国经济社会发展作出了不可替代的重大贡献。<br />
<br />
长期以来，全国广大教师和教育工作者认真贯彻党的教育方针，教书育人，勤勉工作，默默耕耘，无私奉献，为我国教育事业发展付出了大量心血，为我国社会主义建设培养了大批人才，为党和人民作出了杰出贡献。在这里，我代表党中央、国务院，向全国广大教师和教育工作者，向所有关心和支持我国教育事业的广大干部群众和社会各界人士，表示衷心的感谢和诚挚的问候！<br />
<br />
新中国教育的60年，是中国特色社会主义教育体系日益完善的60年。在这一历史进程中，我们不断深化对我国社会主义教育事业发展规律的认识，得出了十分重要的结论。一是教育是国家和民族发展最根本的事业，必须坚持党对教育工作的领导，明确政府发展和管理教育的责任，落实教育优先发展的战略地位，实现教育和经济社会协调发展，充分发挥教育在党和国家事业中的基础性、先导性、全局性地位和作用。二是教育的根本目的是培养德智体美全面发展的社会主义建设者和接班人，必须全面贯彻党的教育方针，把促进学生健康成长作为学校一切工作的出发点和落脚点。三是教育事业发展的生机活力在改革开放，必须始终按照面向现代化、面向世界、面向未来的要求，立足社会主义初级阶段基本国情，坚持继承和创新相结合，不断深化教育体制改革和教育教学改革。四是教育是改善民生、促进社会和谐的重要途径，必须坚持以人为本，促进教育公平，保障公民依法享有受教育的权利。五是教育事业发展的关键在教师，必须紧紧依靠广大教师和教育工作者，遵循教育规律办学教学，不断提高教师政治和业务素质，弘扬尊师重教的社会风气。这些重要结论弥足珍贵，已经在《教育规划纲要》中得到体现，更要在推进教育改革和发展中牢牢把握并不断丰富发展。<br />
<br />
当前，我国社会主义现代化建设已进入新的发展阶段。我们的奋斗目标是，到2020年全面建成小康社会，进而到本世纪中叶基本实现现代化，在中国特色社会主义道路上实现中华民族伟大复兴。优先发展教育、提高教育现代化水平，对实现全面建设小康社会奋斗目标、建设富强民主文明和谐的社会主义现代化国家具有决定性意义。<br />
<br />
随着科技进步日新月异，知识越来越成为提高综合国力和国际竞争力的决定性因素，人力资源越来越成为推动经济社会发展的战略性资源，科技、教育、人才竞争在综合国力竞争中的重要性日益凸显。当今世界的综合国力竞争，说到底是民族素质竞争。教育对提高人民思想道德素质和科学文化素质、发展科学技术、培养人才具有基础性作用。我们要紧紧抓住机遇、有力迎接挑战，坚持优先发展教育，提高教育现代化水平，充分发挥教育重大作用。我们要通过大力发展教育事业，努力培养造就数以亿计的高素质劳动者、数以千万计的专门人才和一大批拔尖创新人才，加快转变经济发展方式，坚持走中国特色新型工业化道路，增强我国发展后劲和国际竞争力，不断推动科学发展。我们要通过大力发展教育事业，在全社会特别是青少年中弘扬社会主义核心价值体系，进一步形成共同的理想信念和道德规范，培育文明风尚，增强民族凝聚力，打牢全党全国各族人民团结奋斗的思想道德基础，不断促进社会和谐。我们要通过大力发展教育事业，全面加强和改进德育、智育、体育、美育，坚持文化知识学习和思想品德修养的统一、理论学习和社会实践的统一、全面发展和个性发展的统一，不断促进人的全面发展。<br />
<br />
我们必须清醒地看到，虽然我国教育事业已经取得巨大成就，但还不完全适应国家经济社会发展和人民群众接受良好教育的要求。主要问题是：有学上的问题基本解决，但上好学的问题依然突出，人民群众不断增长的多样化教育需求还不能得到很好满足；教育观念相对落后，内容方法比较陈旧，中小学生课业负担过重，素质教育推进困难；学生适应社会和就业创业能力不强，创新型、实用型、复合型人才紧缺；教育体制机制不完善，学校办学活力不足，教育结构和布局不尽合理，城乡、区域教育发展不平衡；教育投入不足，教育优先发展的战略地位尚未完全落实。我们要推动教育事业科学发展，必须着力解决这些问题。<br />
<br />
教育是国计，也是民生；教育是今天，更是明天。大力发展教育事业，是全面建设小康社会、加快推进社会主义现代化、实现中华民族伟大复兴的必由之路。全党全国要积极行动起来，坚持育人为本，以改革创新为动力，以促进公平为重点，以提高质量为核心，推动教育事业在新的历史起点上科学发展，加快从教育大国向教育强国、从人力资源大国向人力资源强国迈进，为中华民族伟大复兴和人类文明进步作出更大贡献。<br />
<br />
二、全面推动我国教育事业改革和发展<br />
<br />
推动教育事业科学发展，必须高举中国特色社会主义伟大旗帜，以邓小平理论和“三个代表”重要思想为指导，深入贯彻落实科学发展观，实施科教兴国战略和人才强国战略，优先发展教育，完善中国特色社会主义现代教育体系，办好人民满意的教育，建设人力资源强国。要全面贯彻党的教育方针，坚持教育为社会主义现代化建设服务，为人民服务，与生产劳动和社会实践相结合，培养德智体美全面发展的社会主义建设者和接班人。要全面推动教育事业科学发展，立足社会主义初级阶段基本国情，把握教育发展阶段性特征，坚持以人为本，遵循教育规律，面向社会需求，优化结构布局，提高教育现代化水平。<br />
<br />
关于当前和今后一个时期我国教育改革和发展的总体战略和主要任务，《教育规划纲要》已经作出全面部署，各级党委和政府要按照优先发展、育人为本、改革创新、促进公平、提高质量的工作方针，切实抓好落实，确保到2020年我国基本实现教育现代化，基本形成学习型社会，进入人力资源强国行列。这里，我想就推动教育事业科学发展强调几个问题。<br />
<br />
第一，推动教育事业科学发展，必须优先发展教育。优先发展教育是党和国家长期坚持的一项重大方针。我国未来发展、中华民族伟大复兴，关键靠人才，基础在教育。在党和国家工作全局中，必须始终把教育摆在优先发展的战略地位。要把优先发展教育作为贯彻落实科学发展观的基本要求，切实保证经济社会发展规划优先安排教育发展、财政资金优先保障教育投入、公共资源优先满足教育和人力资源开发需要，并尽快形成科学规范的制度。要转变政府教育管理职能，落实政府发展和管理教育责任，积极推动建设覆盖城乡的基本公共教育服务体系，逐步实现基本公共教育服务均等化。教育投入是支撑国家长远发展的基础性、战略性投资，要健全以政府投入为主、多渠道筹集教育经费的体制，大幅度增加教育投入，2012年实现国家财政性教育经费支出占国内生产总值的4％，并保持稳定增长。要把是否坚持优先发展教育、发挥教育重大作用作为检验各级党政领导班子是否真正贯彻落实科学发展观的重要内容。<br />
<br />
各地区各部门要从自身实际出发，明确分工，落实责任，密切配合，形成推动优先发展教育合力，统筹推进各级各类教育，实现更高水平的普及教育，形成惠及全民的公平教育，提供更加丰富的优质教育，构建体系完备的终身教育，健全充满活力的教育体制。要基本普及学前教育，重点发展农村学前教育，遵循幼儿身心发展规律，坚持科学保教方法，加强学前教育管理，保障幼儿快乐健康成长。要巩固义务教育普及成果，全面提高普及水平，全面提高教育质量，推动义务教育均衡发展，确保适龄儿童少年接受良好义务教育。要加快普及高中阶段教育，合理确定普通高中和中等职业学校招生比例，全面提高普通高中学生综合素质，推动普通高中多样化发展。要大力发展职业教育，加快发展面向农村的职业教育，形成适应经济发展方式转变和产业结构调整要求、体现终身教育理念、中等和高等职业教育协调发展的现代职业教育体系，着力培养学生的职业道德、职业技能、就业创业能力。要全面提高高等教育质量，提高人才培养、科学研究、社会服务整体水平，建成一批国际知名、有特色、高水平的高等学校，加快建设世界一流大学和一流学科，显著增强高等教育国际竞争力。同时，要抓好继续教育、民族教育、特殊教育，推动各级各类教育全面协调发展。<br />
<br />
<br />
第二，推动教育事业科学发展，必须坚持以人为本。坚持以人为本、全面实施素质教育是教育改革和发展的战略主题，是贯彻党的教育方针的时代要求，核心是解决好培养什么人、怎样培养人的重大问题，重点是面向全体学生、促进学生全面发展，着力提高学生服务国家服务人民的社会责任感、勇于探索的创新精神、善于解决问题的实践能力。坚持以人为本，在教育工作中的最集中体现就是育人为本、德育为先。德是做人的根本，只有树立崇高理想和远大志向，从小打牢思想道德基础，学习才有动力，前进才有方向，成才才有保障。要把育人为本作为教育工作的根本要求，加强理想信念教育和道德教育，把社会主义核心价值体系融入国民教育全过程，深入推动中国特色社会主义理论体系进教材、进课堂、进头脑，引导学生形成正确的世界观、人生观、价值观，坚定学生对中国共产党领导、社会主义制度的信念和信心，培养学生团结互助、诚实守信、遵纪守法、艰苦奋斗的良好品质，树立社会主义民主法治、自由平等、公平正义理念。要把德育融入学校课堂教学、学生管理、学生生活全过程，创新德育观念、目标、内容、方法，充分体现时代性，准确把握规律性，大力增强实效性。<br />
<br />
坚持以人为本，在教育工作中的重要着眼点是全面提高国民素质。这就需要全面实施素质教育。实施素质教育不仅涉及教育各个阶段和领域，更涉及文化传统、经济发展、社会结构、用人制度等方方面面，必须统筹兼顾、协调推进，切实把实施素质教育这件大事抓紧抓好、抓出成效。人力资源是经济社会发展的第一资源，教育是开发人力资源的主要途径。要以学生为主体，以教师为主导，充分发挥学生的主动性，尊重教育规律和学生身心发展规律，为多样化、个性化、创新型人才成长提供良好环境和机制，着力培养我国现代化建设需要的各方面人才，特别是要高度重视培养拔尖创新人才。要促进学生全面发展，优化知识结构，丰富社会实践，加强劳动教育，着力提高学习能力、实践能力、创新能力，提高综合素质，加快改变学生创新能力培养不足状况。要把人人可以成才的观念贯穿教育全过程、贯穿社会各行各业，把培养人的创造性和培养拔尖创新人才有机统一起来，推进小学、中学、大学有机衔接，教学、科研、实践紧密结合，学校、家庭、社会密切配合，加强学校之间、校企之间、学校和科研机构之间合作以及中外合作等多种联合培养方式，更加重视打牢创新基础、倡导创新精神、激发创新活力，更加重视发展创新文化、完善创新机制、营造创新氛围，大幅提高教育培养创新人才能力和水平。要以终身学习理念引领学校教育改革，构建灵活开放的终身教育体系，努力做到学历教育和非学历教育协调发展、职业教育和普通教育相互沟通、职前教育和职后教育有效衔接，为努力形成全民学习、终身学习的学习型社会奠定坚实基础。<br />
<br />
第三，推动教育事业科学发展，必须坚持改革创新。改革是教育事业发展的强大动力。要深化教育教学改革，重视改革的系统设计和整体安排，加快重要领域和关键环节改革步伐，以改革推动发展，以改革提高质量，以改革增强活力，进一步消除制约教育发展和创新的体制机制障碍，全面形成与社会主义市场经济体制和全面建设小康社会目标相适应的充满活力、富有效率、更加开放、有利于科学发展的教育体制机制。<br />
<br />
深化教育体制改革，关键是更新教育观念，核心是改革人才培养体制，目的是提高人才培养水平。教育成效不应只看学生是否能准确填写标准答案，更要看学生的学习能力、实践能力、创新能力，看他们是否掌握了发现问题、解决问题的关键能力，看他们是否具备了高度的社会责任感。要树立全面发展观念、人人成才观念、多样化人才观念、系统培养观念、终身学习观念，形成体系开放、机制灵活、渠道互通、选择多样的人才培养体制。要创新人才培养模式，适应国家和社会发展需要，遵循教育规律和人才成长规律，注重学思结合、知行统一、因材施教，创新教育教学方法，倡导启发式、探究式、讨论式、参与式教学，激发学生好奇心，发挥学生主动精神，鼓励学生进行创造性思维，改变单纯灌输式的教育方法。要把中小学生从繁重的课业负担下解放出来，使广大青少年在发掘兴趣和潜能的基础上全面发展。<br />
<br />
深化教育体制改革，要正确处理政府、学校、社会的关系，落实和扩大学校办学自主权，建设依法办学、自主管理、民主监督、社会参与的现代学校制度。要深化办学体制改革，坚持教育公益性原则，健全政府主导、社会参与，办学主体多元、办学形式多样、充满生机活力的办学体制，积极鼓励行业、企业等社会力量参与公办学校办学，引导社会资金以多种方式进入教育领域，大力支持民办教育，形成以政府办学为主体、全社会积极参与、公办教育和民办教育共同发展的格局。要深化教育管理体制改革，以转变政府职能和简政放权为重点，提高公共教育服务水平，明确各级政府责任，规范学校办学行为，形成政事分开、权责明确、统筹协调、规范有序的教育管理体制。在教育改革中，要正确处理改革发展稳定的关系，坚持改革力度、发展速度、社会可承受程度的统一。<br />
<br />
在经济全球化深入发展、我国参与国际经济合作和竞争日益广泛深入的新形势下，我们必须加强教育国际交流合作，提高教育交流合作水平，充分利用国内国际两种教育资源。要借鉴国外先进教育理念和有益教育经验，引进优质教育资源，提升我国教育的国际地位和影响力、竞争力。要积极吸引更多世界一流专家学者来华从事教学、科研、管理工作，有计划引进海外高端人才和学术团队，推动我国高水平教育机构海外办学。要坚持支持留学、鼓励回国、来去自由的方针，创新和完善公派出国留学机制，提高对留学人员的服务和管理水平，吸引海外优秀留学人员回国服务。<br />
<br />
第四，推动教育事业科学发展，必须促进教育公平。教育公平是社会公平的重要基础。坚持教育的公益性和普惠性，把促进公平作为国家基本教育政策，是促进社会公平的重要基础性任务。教育公平的关键是机会公平，基本要求是保障公民依法享有受教育的权利，重点是促进义务教育均衡发展和扶持困难群众，根本措施是合理配置教育资源。近年来，我国在保障教育机会公平方面迈出重大步伐，我们要在这个基础上继续促进公民受教育机会公平，推动教育公平不断迈上新台阶。要着力促进公共教育资源配置公平，加强薄弱环节和关键领域，加快缩小城乡、区域教育发展差距。要健全国家资助政策体系，逐步对农村家庭经济困难和城镇低保家庭子女接受学前教育予以资助，提高农村义务教育家庭经济困难寄宿生生活补助标准，改善中小学生营养状况，建立普通高中家庭经济困难学生国家资助制度，完善普通本科高校、高等职业学校、中等职业学校家庭经济困难学生资助政策体系，设立研究生国家奖学金，完善助学贷款体制机制。要进一步加大农村、边远贫困地区、民族地区教育投入，启动民族地区、贫困地区农村小学生营养改善计划，改善农村学生特别是留守儿童寄宿条件，努力不让一个孩子因家庭经济困难、就学困难或学习困难而失学。<br />
<br />
促进教育公平，要着力促进教育制度规则公平，全面推进依法治教和依法治校，坚持用规范管理维护教育公平，探索教育行政执法体制机制改革，完善督导制度和监督问责机制。要实施好高校招生“阳光工程”，建立和完善高校毕业生就业服务体系，坚决治理教育乱收费。要依法落实民办学校、学生、教师与公办学校、学生、教师平等的法律地位。<br />
<br />
推进教育公平是复杂的社会系统工程，也是一个需要逐步实现的历史过程。要坚持在经济社会发展的基础上，以发展促公平，以改革促公平，以政策支持促公平，不断满足广大群众日益增长的多层次、多样化教育需求。<br />
<br />
第五，推动教育事业科学发展，必须重视教育质量。全面提高教育质量，对建设教育强国、人力资源强国意义重大。要把提高质量作为教育改革和发展的核心任务，摆在各级各类教育更加突出的位置，树立以提高质量为核心的教育发展观，坚持规模和质量的统一，注重教育内涵发展。要把促进人的全面发展、适应社会需要作为衡量教育质量的根本标准，努力协调好教育发展和人的全面发展、教育发展和社会发展的关系，鼓励学校办出特色、办出水平和出名师、育英才。要把教育资源配置和学校工作重点集中到强化教学环节、提高教育质量上来，建立以提高教育质量为导向的管理制度和工作机制，制定教育质量国家标准，建立健全教育质量保障体系。要深入研究经济社会发展和用人单位对人才结构培养质量需求变化情况，加强教育质量评估和监管。<br />
<br />
<br />
教育大计，教师为本。要把加强教师队伍建设作为教育事业发展最重要的基础工作来抓，充分信任、紧密依靠广大教师，进一步激发和保护他们投身教育改革创新、推动教育事业发展的积极性、主动性、创造性，着力提升教师素质、优化队伍结构，着力加强中青年教师和创新团队建设，健全教师管理制度，努力造就一支师德高尚、业务精湛、结构合理、充满活力的高素质专业化教师队伍。要采取更有力的措施，提高教师地位，维护教师权益，改善教师待遇，加强教师培训，关心教师身心健康，依法保证教师平均工资水平不低于或者高于国家公务员平均工资水平并逐步提高，落实教师绩效工资，对长期在农村基层和艰苦边远地区工作的教师实行倾斜政策，落实和完善教师社会保障政策，为教师解决后顾之忧，为教师发展成长创造更多机会、提供更有利的条件。要吸引优秀人才从事教育工作，支持和鼓励他们长期从教、终身从教。要在全社会大力弘扬尊师重教的优良传统，宣传优秀教师先进事迹，使教师成为最受社会尊重的职业。要加强学校领导者、管理者队伍建设，加强辅导员、班主任队伍建设，提高队伍整体素质。要完善绩效考核，发扬良好校风、教风、学风，力戒浮躁，形成全面提高教育质量的良好氛围。<br />
<br />
要以教育信息化带动教育现代化，把教育信息化纳入国家信息化发展整体战略，加快教育信息基础设施建设，超前部署教育信息网络，加强优质教育资源开发和应用，构建国家教育管理信息系统，确保到2020年基本建成覆盖城乡各级各类学校。的教育信息化体系，促进优质教育资源普及共享，加快全民信息技术普及和应用。<br />
<br />
三、全党全社会共同为推动教育事业科学发展而奋斗<br />
<br />
推动教育事业科学发展，加强和改善党的领导是关键。贯彻落实好《教育规划纲要》是摆在全党全社会面前的重大任务，迫切要求各级党委、政府和领导干部提高领导教育工作能力和水平。各级党委和政府要提出实施方案，加强领导，明确目标，制定措施，加大投入，切实把《教育规划纲要》贯彻好、落实好。<br />
<br />
第一，着力加强和改善对教育工作的领导。各级党委和政府要强化责任意识，正确处理教育发展与经济社会发展的关系，健全领导体制和决策机制，以更大的决心、更多的财力、更多的精力支持教育事业，加强教育宏观政策和发展战略研究，制订符合本地实际的教育发展规划和政策措施，抓好《教育规划纲要》提出的重大项目和改革试点，及时研究解决教育改革和发展的重大问题和群众关心的热点问题。要把制度建设作为推动教育事业科学发展的重要着力点，深化改革，勇于创新，增强改革措施的针对性、协调性、实效性，建立健全统筹教育和经济社会发展、统筹教育改革和对外开放、统筹城乡和区域教育发展、统筹各级各类教育发展的机制，建立健全推进教育公平、实施素质教育、培养创新人才的有效制度体系，建立健全有利于推动教育事业科学发展的综合评价体系。要加强和改进教育系统党的建设，健全各级各类学校党的组织，坚持和完善公办高等学校党委领导下的校长负责制，加强学校领导班子和领导干部队伍建设，加强在优秀青年教师、优秀学生中发展党员工作，重视学校共青团、少先队工作，充分发挥党组织在教育改革和发展中的作用。要坚持社会主义办学方向，牢牢把握党对学校意识形态工作的主导权，加强和改进学校思想政治工作，加强校园文化建设。要加强教育系统党风廉政建设和行风建设，完善体现教育系统特点的惩治和预防腐败体系，严格执行党风廉政建设责任制，积极推行政务公开、校务公开，逐步推行基层党务公开，坚决惩治和有效预防腐败，坚决纠正损害群众利益的各种不正之风。要深入开展平安校园、文明校园、绿色校园、和谐校园创建活动，加强校园网络管理，加强师生安全教育和学校安全管理，加强校园和周边环境治安综合治理，切实维护教育系统和谐稳定。<br />
<br />
第二，着力动员全社会共同推动教育事业科学发展。教育振兴，全民有责。实现教育改革和发展目标，必须发挥人民群众聪明才智、动员全社会一起努力，充分调动全社会关心和支持教育的积极性，共同担负起培育下一代的责任。要鼓励和支持全社会支持和参与教育事业发展，努力形成推动教育事业科学发展和促进人的全面发展的社会氛围。要切实保障人民群众对教育的知情权、参与权、表达权、监督权，建立和完善群众利益表达渠道和对教育建言献策的平台，积极利用社会力量监督和评价教育、参与教育管理。要完善相关政策，发动更多社会力量支持教育、投入教育、兴办教育。要引导和鼓励社会各界更新教育观、成才观、用人观，支持教育改革创新，形成既生动活泼又规范有序、共同推动教育事业科学发展的生动局面。<br />
<br />
<br />
第三，着力组织广大教师和教育工作者投身教育事业科学发展伟大实践。广大教师和教育工作者是推动教育事业发展的生力军。推动教育事业科学发展，必须紧紧依靠广大教师和教育工作者。广大教师要增强教书育人的责任感和使命感，把教学作为首要任务，自觉加强师德建设，大力弘扬优良教风，努力提高业务水平，积极探索和实践教育教学规律，关爱学生，严谨笃学，淡泊名利，自尊自律，全心全意帮助学生全面发展，做学生健康成长的指导者和引路人。学校各级领导干部和管理人员要用主要精力抓好人才培养和学校管理，改进领导方式，发扬校内民主，开放社会监督，以优异工作业绩赢得教师、学生、家长信任和支持。<br />
<br />
同志们，21世纪是中华民族伟大复兴的世纪，也必将是我国教育事业全面发展的世纪。全党全国要积极行动起来，真抓实干，开拓进取，努力推动教育事业科学发展，为把我国建设成为人力资源强国、人才强国、创新型国家而不懈奋斗！<br />
<br />
