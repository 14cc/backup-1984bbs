																											<div id="postmessage_757522" class="t_msgfont">“孤独者” <br />
<br />
一直到父亲死了，儿子潘琦才觉得自己对他有了一些了解。整理父亲遗物时，他对妈妈说：“我在南京，每月的生活费都比爸爸一个月的工资高。”<br />
<br />
这个23岁的年轻人，这个夏天刚从大学毕业。从小，他跟随姥姥姥爷长大。上大学后，每个月姥爷要给他寄去1100元的生活费。<br />
<br />
“那边消费高，儿子个子高，吃的多，我也不能控制他。”冯萍说。多年来，她苦心经营，努力为儿子制造着一个相对舒适的环境。“如果靠我们两人，压根不可能供孩子上这个大学啊。”<br />
<br />
上大学后，每次放假回来，潘琦都去看爸爸。爸爸出事前的这半年，他回西安实习，父子俩交流比较多一些。潘琦说，两人在一起，爸爸平时不会讲自己的工作，或许他觉得，天天孤零零上夜班看门，有啥可讲的呢。儿子就给他讲学校的事情，“啥都讲”。<br />
<br />
在潘琦的印象中，最后这一年多，爸爸住的地方，是最深的巷子里“见不到阳光的角落”。但这并不妨碍父子俩享受他们的生活乐趣。<br />
<br />
“爸爸做啥饭都好吃，尤其是面。他自己和面、擀面，擀的面特筋道。”潘琦一米八三的个头，高大帅气。他知道，爸爸是以他为骄傲的。他也知道，别人曾调侃爸爸。“就你那点工资，连双鞋都给娃买不起。”<br />
<br />
今年大年三十晚上，潘琦陪姥姥姥爷吃完饭，去找爸爸。爸爸在车间值班，跑出来两个小时，爷俩一起在家喝了酒。<br />
<br />
“他值夜班几年了，冬天披个军大衣，揣两个蛋糕就走，他牙不好。夏天穿布鞋，喜欢用矿泉水冻一瓶冰，带着去上班。”这是儿子对父亲的记忆。<br />
<br />
他知道爸爸没钱，从不向爸爸要钱。潘鸿强呢，每次吃完饭要去上班，总是要塞给儿子一点钱，一般都是五十，还有二三十的，儿子懂事，有时就给他留下了。<br />
<br />
“我特别难受的是，他老是一个人上夜班，厂房那么大，他一个人，就那么一直孤零零的。”潘琦说。<br />
<br />
遗书上的“孤独者”几个字，让冯萍想起来就难过。她觉得他命太苦。襁褓中母亲就死了，和父亲哥哥相依为命，后来哥哥也煤气中毒死了。“一生都孤苦伶仃的。”<br />
<br />
这个49岁的失意男人，一生没到别的地方去过。除了他生活的西安，只在姑姑去世时，回了两次河南。<br />
<br />
在他死后，冯萍和儿子去与厂里交涉。厂里表示，按照相关法规，潘鸿强的自杀和厂里并没有关系，厂里只能给3000元的丧葬费。厂里同时“纠正”了他一个月只有850元工资的说法，说这些工资是扣掉“三金”之后的。另外，潘鸿强还有半年奖和年终奖等。但冯萍算了一笔账，即使这样，他每月拿到的工资也不过1000元钱左右。<br />
<br />
厂方并不认为是工资太低造成他的困境。“按说工资还行吧。我们还有环卫工人，一个月才五六百元。”一位车间负责人说。<br />
<br />
7月23日，厂方给出了最后的处理结果：给付潘琦抚慰金3万元。潘琦表示不能接受。<br />
<br />
“其实我只是想弄明白，压垮我父亲的究竟是什么。”这个在父亲死后一直很沉默的年轻人说。</div>

							
							
							
							
