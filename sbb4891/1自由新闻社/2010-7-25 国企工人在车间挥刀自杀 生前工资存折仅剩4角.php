																											<div id="postmessage_757520" class="t_msgfont">四角六分钱 <br />
<br />
冯萍本已忍住眼泪，可拿起一家人的照片时，又哭了。<br />
<br />
照片是2006年儿子考上了大学时，“一家三口”的合影。照片上，她站在两个男人中间，儿子高大帅气，潘鸿强也很精神。他从年轻时就那样，两道很浓的剑眉。40多岁了，拾掇拾掇，“还蛮帅”。<br />
<br />
“瞧这张照片，儿子太像他爸了。”她说，那是另一张黑白照片，照片上的工人潘鸿强，留着80年代的大背头，尖领白衬衫，浓眉大眼，很俊朗。那是他曾经的青春。<br />
<br />
他们在1985年相识，结婚。她家境优裕，而他是穷小子，从小没有娘，也没有兄弟姐妹，就和当工人的老父亲相依为命。或许是同情，或许是缘分，她不顾家里反对，和他好了。<br />
<br />
结婚两年，他们有了儿子，从小平房搬到了简易楼房，但日子一直过得紧巴。作为工厂里最普通的工人，他的工资一直很低。这么多年过来，到2010年，他每个月拿到手的工资扣掉“三金”之后是850元。<br />
<br />
日子紧，钱少，女人的委屈多得一箩筐。两个人也常常说不到一块儿去。“他是老工厂工人的样子，你能感觉到他老是缩着，缩着。”<br />
<br />
2004年，他俩协议离了婚。离了，可她也没有离开家。一直到2007年5月，“最终下决心离开了那个家。”“离婚最主要是为了儿子！他工资太低了。离了我可以办低保，可以回娘家。孩子也可以名正言顺地让姥爷姥姥管。靠他，孩子上学咋办？”女人说到这里，哭得更伤心。<br />
<br />
儿子是军校委培生，一年学费9500元，基本上都是姥爷姥姥出。离了婚的冯萍办了低保，在外边打零工，有时兼两份工，推销东西，在网吧帮忙，一个月收入有两三千元，比他强。有时，他手头实在没钱，她就给他一两百元。<br />
<br />
最后一次见面，他在电话里说：“萍，我没钱了，借你200元。等发工资还。”她说：“你用呗！还借啥呀。”他们在附近一个酒店门口碰面。拿了钱，他匆匆走了。<br />
<br />
6月12日是冯萍生日，他们在电话里说好要和儿子聚聚呢！可6月10日，潘鸿强死了。<br />
<br />
死去的潘鸿强身无分文。留下的遗物除了钥匙、手电筒，就是一部欠费47元的三星手机。冯萍充上电，交了话费，“作为永久的纪念。”<br />
<br />
能作为纪念的，还有一张工资存折，里边只剩下0.46元。<br />
<br />
工龄31年的潘鸿强，每月发到手的工资是600元，由厂里发现金，然后，车间再给这个存折上补发250元。<br />
<br />
这张2009年12月9日新换的存折显示，从2010年1月26日起，每月25日打入的250元工资，潘鸿强都是很快取走。在下次发放之前，存折里一般都只剩四五十元。<br />
<br />
2010年5月9日，他取出了90元，卡里剩下4.06元；5月25日发了250元，当天他取了200元，次日又取了54元，卡里剩下0.46元。<br />
<br />
在潘鸿强死后半个月，车间往存折上打入了250元的上月工资。只是这次，他没有再等到发工资的日子。<br />
<br />
窘迫的日子 <br />
<br />
“他是撑不住了。”耿田刚说。<br />
<br />
在耿田刚的记忆里，十几年前的潘鸿强也有快乐的日子。那时，他没离婚，周围的人，又和他一样――差不多地穷。除了自己的工资，还有父亲的退休金，日子还过得去。<br />
<br />
2000年，华山厂开始有数百工人下岗，到2005年，前后有几千人下岗。潘鸿强所在的车间因工种特殊，下岗的人不多，加上他是老工人，留了下来。可在耿田刚看来，没有下岗的潘鸿强算不上幸运。<br />
<br />
他们是20多年的同事、朋友。2004年，同是车间工人的耿田刚和妻子相继下岗。为了生活，耿田刚和妻子一起做生意，折腾了几年，渐渐有了起色。如今，他们虽然不是很富裕，但“和工厂相比，已经强多了”。大约一个月前，耿田刚装修好了新房。虽然还背着一点债，但生活不会受影响。<br />
<br />
而潘鸿强依旧围着机器转着。<br />
<br />
大约三四年前，车间开始实行计件工资，潘鸿强腰有伤，加上患糖尿病，干不动活。别人能出100多个活，他最多出60个。有几个月才拿二三百元工资。最后，还是朋友托人说话，车间照顾他，才有了这个轻松点的“值班”岗位。所有工资加起来，每月能拿850元。<br />
<br />
可他依旧为钱发愁。在耿田刚的印象里，这几年，潘鸿强的日子越来越拮据。“我常接到他的电话，说没饭吃了。有时叫他到家里来吃，有时给他点钱，有时一二百，有时二三百元。作为朋友，哪个月我不给他买两条烟！”<br />
<br />
潘鸿强平时抽4元钱一包的“延安”。每个月发了工资，先买米面油，再买两条烟。可到月底，往往就“弹尽粮绝”了。<br />
<br />
耿田刚认为这个老朋友并不是大手大脚的人。“实在是工资低，用钱的窟窿也太多了。别人有家，两个人撑着。他一个人，也没有兄弟姐妹。连个‘混饭’的地方也没有。加上看病、租房、交房贷，确实困难！”他说。<br />
<br />
潘鸿强所在的车间有互助工会。近一年多来，几乎每个月，他都要向工会借款一二百元。<br />
<br />
潘鸿强也想过改变，谋划着“干点啥”。可干啥呢？他没本钱，也没有亲戚朋友可借。何况，围着机器转了30多年的他，又会干啥呢？<br />
<br />
“现在这时代啊，有智吃智，无智吃力。他啥都没有。再说，在工厂呆那么长时间，人的脑子都木了。他也想去应聘，但以他的年龄、身体，出去也就是给人看个门。”耿田刚说。<br />
<br />
这个失意的中年男人，最终日子越过越拮据。“据我所知，他常常是饥一顿，饱一顿。去他那里，常常是空空荡荡地，啥都没有。”耿田刚说。<br />
<br />
而他又好面子。红白喜事，别人要掏二百，他咬咬牙，也要掏。“他爱充大头”，工友的妻子这样说。<br />
<br />
而在朋友眼中，潘鸿强是个重感情、讲义气的人。“他心性强，在人跟前从不示弱。谁想在他跟前说风凉话，没门！”耿田刚说，虽然潘鸿强对人都很和气，脾气也好，但并不是嘻嘻哈哈的人。这个在朋友眼中“很硬气”的男人，平时的爱好是喝点酒。<br />
<br />
今年春节，他们两家人聚了一次，吃烤鸭。那次他看起来还算高兴。“多年没见他开心了！其实有啥开心的事呢，又不像人家有房有车。去年，要分房了，本来是高兴的事，对他，倒成了灾难。买了一屁股债。”耿田刚叹息着。</div>

							
							
							
							
