																											<div id="postmessage_757521" class="t_msgfont">沉重的房子 <br />
<br />
在亲人和朋友的眼中，压垮潘鸿强的“最后一根稻草”，是房子。<br />
<br />
结婚后，潘鸿强一直住在一个叫新立村的厂区宿舍。那是个大杂院，住了大约100多户人家，几乎都是车间的工人。<br />
<br />
新立村的日子是熟悉而亲切的。他在那里出生长大，周围都是老邻居、穷朋友，常常，人们下班回来，在门口一站，就能聊半个小时。在大院里，大家都喊他“小民”，那是他的小名。<br />
<br />
一直到出事前，新立村都是潘鸿强最愿意去的地方。虽然近两年厂里要拆迁，老邻居们都搬走了。可他还是不习惯，常常拐弯到院子里去看看。<br />
<br />
2004年左右，工厂住房改革。厂里在旁边的车间腾出一块地方盖福利房。这些一辈子住平房、简易楼房的工人们，也可以住商品房了。“虽然是好事，可没有几家不愁的。家里都紧，要买房都差钱啊。”冯萍说。<br />
<br />
夫妇俩交了房子的4万元首付款。冯萍说，家里没积蓄，全是借的，要付利息，比银行略高一些。收房交钥匙时，实际面积90多平米，比当初登记的大点，又补交了4000多。<br />
<br />
2009年初，要拆迁了，厂里要收回院子租出去。潘鸿强在附近的韩北村租了一间约25平米的民房。<br />
<br />
等新房装修时，潘鸿强发现自己根本无能为力。他没和冯萍商量，就和工友换了房子，把90多平米换成了70多平米。人家给他补了1万元。他又借了些钱，开始装修。<br />
<br />
冯萍能看出，他是用心去装修这个房子的。他自己设计，卧室铺成复合木地板，客厅铺瓷砖。这毕竟是他一生第一次拥有自己的房子。可装修太花钱，常常就没钱了，停停装装，一直到去年5月装好，花了约6万元。此时，潘鸿强已经是负债累累。“不知道这段时间他是怎么过的。”等冯萍知道时，潘鸿强已经把房子卖给了同事。70多平米的房子，连房子带装修，总共卖了16万元。“知道房子卖掉了，我心都凉了。”冯萍说，可想着那么多债务逼着他，也实在是没办法啊！<br />
<br />
卖了房子，他把2004年借的房款和利息、装修款还了，加上还了一些债，填了其他一些“窟窿”。冯萍说，最终，他只是“原吃原，打了个平手。”可在他死后，至今还有3万元的银行房贷。<br />
<br />
他又住进了租来的房子。卖房的事一直也没给别人说。<br />
<br />
这个“人生的失败者”，最终没有为儿子，也没有为因贫穷离开的妻子，实现曾经的承诺。他最终没有拥有这套让他付出了心血和梦想的房子。<br />
<br />
“翻不过身来” <br />
<br />
傍晚，78岁的苏玉芳在幸福小区的垃圾桶里翻拣瓶子。<br />
<br />
说起潘鸿强，老太太就抹眼泪。“给他多烧点纸，在这边（活着时）难场，在那边不要也难场。”她拉着冯萍的手，絮叨着。<br />
<br />
老人是华山厂的老职工，退休了。儿子吕洪生和潘鸿强也是“老兄弟”，老家都是河南的，又是老邻居。潘鸿强活着时，有事没事也爱来他家转。他们都知道彼此的“难场”。<br />
<br />
2001年，吕洪生也下岗了，妻子也离了婚。19岁的儿子因受到其他刺激得了精神分裂症，每月都要吃药，一犯病就砸人家车玻璃。如今，一家老少三口指着老太太的退休金每月1030元还有孙子一个月290元的低保过日子。<br />
<br />
幸福小区砖混结构的房子，一平米998元。这看起来已经极低的房价，对他们来说，也是不小的数字。事实上，相当多的普通工人家庭，为买房子都背了债。吕洪生家这套60多平米的两居室，是向姐妹们借钱买下来的。没钱装修，水泥地抹了抹，墙自己刷了刷，就搬进来了。<br />
<br />
下岗后，吕洪生在外打零工，这么几年，也没有折腾出样子。“我一个大男人，出门脸上都火辣辣的。”他说。<br />
<br />
不过对潘鸿强的死，他表示想不通。“这不，孩子就要大学毕业了，日子也会慢慢好起来啊。”<br />
<br />
“他为什么要走这样的路？”记者问。<br />
<br />
“他老是有一种翻不了身的感觉。老觉得自己比人低，啥都比别人低。”冯萍说。<br />
<br />
“他一个人孤独，又不愿示弱。”这是耿田刚的答案。<br />
<br />
不过耿田刚也认为，潘鸿强的处境并不是特例。其实在他们工厂，大家的生活都不太好。<br />
<br />
“工人中午都是买点面或馒头，随便吃点。前些天我有事找工友帮忙，完了请大家吃饭，上两个好菜，几下子全没了。看得我心酸。”耿田刚说。<br />
<br />
他举例说明工人们的窘况：前年，院子里的“石头哥”和几个人打牌，牌掉地下了去捡，犯脑溢血，送到医院去，在场的三个人掏遍全身就凑出了12元钱。人最终没有救过来。</div>

							
							
							
							
