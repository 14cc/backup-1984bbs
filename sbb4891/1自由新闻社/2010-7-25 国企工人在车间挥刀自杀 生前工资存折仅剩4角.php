2010年企业工资指导线发布 基准线11% 比前年下调0.5%<br />
<br />
今年企业工资涨幅不得超16%<br />
<br />
昨天，市人保局发布本市2010年企业工资指导线，职工平均工资增长基准线定为11%，上线（预警线）为16%，下线为3%。市人保局强调，企业因经营困难没涨薪甚至降薪，必须经与工会或职工代表协商方可；生产一线及技术工人的平均工资没涨，经营者不得给自己涨薪。<br />
<br />
■有利于管理国企工资总额<br />
<br />
据市人保局介绍，工资指导线是政府向企业发布的年度工资增长水平的建议，能为工资集体协商以及确定工资增长水平提供依据，同时对国企工资总额管理意义重大。<br />
<br />
市人保局还提出了具体贯彻实施意见：生产发展正常的企业，可以参照基准线确定工资增长水平。经济效益增长较快、工资支付能力较强的企业，可以在基准线和上线之间确定工资增长水平。企业在当年经济效益有所下降时，可以参考下线安排本企业工资增长水平；经营亏损、职工工资发放出现困难的企业，经与工会或职工代表协商，可以安排本企业工资零增长或负增长，但支付提供正常劳动职工的工资不得低于本市最低工资标准。</div>

							
							
							
							
