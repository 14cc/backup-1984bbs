核心提示 <br />
<br />
在写给儿子的遗书上，他罗列了自己欠别人的900元债务，“谁看见谁帮我解决”。<br />
<br />
自称“孤独者”的潘鸿强，生前是一名有31年工龄的国有企业工人。他的遗物之一是一张工资存折，死前存折里只剩下0.46元。<br />
<br />
是什么“杀”死了潘鸿强？他的死是性格悲剧？还是在这个激流勇进、适者生存的社会中，中国传统产业工人彷徨转型的一个失败案例？<br />
<br />
49岁的潘鸿强一直有个心愿，好好买块墓地，把父亲的骨灰安葬了。<br />
<br />
这个心愿他揣了14年，可最终也没有实现。6月10日清晨，潘鸿强死了。他用一把刀结束了自己的生命。<br />
<br />
在写给儿子的遗书上，他放下父亲的骨灰寄存证，并在遗书中罗列了自己欠别人的900元债务，“谁看见谁帮我解决”。<br />
<br />
自称“孤独者”的潘鸿强，生前是一名有31年工龄的国有企业工人。他的遗物之一是一张工资存折。截至他死前的5月26日，存折里只剩下0.46元。<br />
<br />
清晨的死亡 <br />
<br />
6月9日晚6时左右，潘鸿强像往日一样去上班。<br />
<br />
走出西安东郊韩北村那间巷子最深处的民房，穿过约50米的巷子，拐两拐，就到了大路上。步行不到10分钟，就到了位于幸福路的华山厂大门口。<br />
<br />
正值下班，工人们涌出厂门，三三两两地回家。熙熙攘攘的人群中，潘鸿强低着头，步子比往常快一些。快到大门口时，“老伙计”耿田刚的女儿喊了他一声“伯伯”，他好像没听见，就过去了。<br />
<br />
前一天下午，耿田刚在路上最后一次见到潘鸿强，他也是匆匆忙忙的，打个招呼就走了。交往20多年，耿田刚感觉潘鸿强这半年来变化很大，比以前沉闷，不太爱和人接触。<br />
<br />
夏天的傍晚很热，潘鸿强穿过工厂的办公区，到了后面的厂区。这里的一草一木，他都很熟悉。他在这里出生、长大，顶父亲班成为一名工人，已经31年。大约3年前开始，他的岗位就是“值班”，也就是夜间看守厂房。<br />
<br />
这是一座大约三四个教室那么大的老式厂房，有两层楼高，里边堆满笨重的机器。他的任务是定时拿手电筒巡视车间。<br />
<br />
厂里本来是安排两个人值班，但为了多一天时间休息，两人约定，隔一天上一次班，这也得到了车间的默许。所以，夜间值班其实只有一个人。<br />
<br />
大部分时间，走在空荡荡的厂房，潘鸿强能看到的，除了那些沉重的机器，就是自己的影子。累了，他也会偶尔和衣在长凳上打个盹儿。<br />
<br />
大约晚上10点，车间一位负责人还见到他。“放心，这里有我没问题！”他说。<br />
<br />
凌晨1点多，他接到一个朋友打来的电话。这个朋友有点口吃，他俩通话有十多分钟，闲聊中，他没有表现出异常。<br />
<br />
公安机关调取的车间摄像头显示，出事前，他曾在车间里绕行好几圈，行为反常。<br />
<br />
惨烈的死亡随即发生。几分钟后，他用一把机床刀挥向自己的脖子。<br />
<br />
上午7时许，最早来上班的工人发现了倒在血泊中已死去的潘鸿强。死亡的时间被公安机关认定为清晨6时。<br />
<br />
耿田刚是第一个接到电话赶往现场的人。“路上我还想，那么硬气的一个人，不可能是自杀，说不定是和小偷搏斗受伤的吧。”<br />
<br />
前妻冯萍闻讯赶到潘鸿强租的房子，开门迎面看见床上放的报纸，上面放着两张纸，那是他写在工厂记录上的“留言”。第一页写着他欠两个工友的账，一个300元，一个600元，共900元，还有信用卡欠款3000元。“谁看见谁帮我解决一下”。<br />
<br />
有一页专门写给儿子：“从今往后你要全力地工作，为人做事一定靠本人，善待别人，生活一定要有记（计）化（划），别不多说，再见了，永别了。”落款“孤独者潘鸿强”，时间是2010年6月3日，距离他死前一星期。<br />
<br />
遗书上压着一个黑色的小证件，那是潘鸿强父亲的骨灰寄存证。<br />
<br />
“看到这个骨灰证，我就啥都明白了！”冯萍说，潘鸿强的父亲1996年车祸去世，因为当时手头紧，没有安葬，骨灰就一直寄存在殡仪馆。这些年他最大的心愿就是给父亲买个墓地。<br />
<br />
“这现在是我的心愿了。好好买两个墓地，把他们父子安葬了。”7月12日，眼睛红肿的冯萍说。<br />
