													<h2>红三代叶家公主大婚 紫禁城内皇室气氛浓重</h2>
						
						
																											<div id="postmessage_243996" class="t_msgfont">2009年9月9日，叶剑英的小孙女叶明子在太庙成婚。整个婚礼用红色玫瑰布置全场，突出浓郁中国风情。白色蕾丝婚纱由身为服装设计的的叶明子亲自设计，并选择卡地亚珠宝作为搭配。 <br />
<br />
　　叶明子，于1979年出生在北京，是中国十大元帅之一叶剑英的孙女，叶明子的名字是她那战功显赫的爷爷叶剑英为她取的，爷爷期望她“明月如心”。明子是爷爷的掌上明珠，从小在老人身边长大。 <br />
<br />
　　叶明子父亲是叶剑英第三子叶选廉，原为保利集团下属凯利公司董事长兼总裁。母亲苏丹丹（现名叶苏丹丹）原为解放军总政歌舞团的报幕员，婚后息艺学画，曾到中国画院进修，后随丈夫叶选廉到香港经商。 <br />
<br />
　　由于其特殊的身份，因此其“新郎是谁”也受到人们的广泛关注。但从目前的报道及其坊间说法来看，其丈夫是一位美国的银行家，具体情况不详。 <br />
 <br />
　　叶明子，于1979年出生在北京，是中国十大元帅之一叶剑英的孙女，也是伏明霞原经纪人叶静子的妹妹。7岁随父母移居香港，13岁只身到英国伦敦求学，之后考入了全球最著名的艺术设计大学圣马丁(Saint Martins)进行深造，毕业作品入围优秀毕业生时装展；大学研修期间前往东京，在ISSE MIYAKE（三宅一生）集团公司进行短期学习、工作；留日期间遇到吉本兴业株式会社经纪人，马上被推荐到日本音乐教父小室哲哉旗下，小室为她谱下《听你的心 Listen To You Heart》一曲；目前在北京设立设计工作室。 <br />
目前，叶明子的客户包括电影导演、艺人、阔太太等。 <br />
<br />
　　“他们想要一些与别人不一样的东西。我做的市场不大，但客人都非常注重高级时装和品质。一般太太们比较喜欢不是很夸张但是看起来很华贵的衣服。但娱乐界的人就喜欢比较夸张、比较显身材的这种衣服。我会根据他们需求的不同去设计不同款式的衣服。另外，一些有政治背景的人要去国外接见什么人，他们不想让人家看我们中国没有好的时装，也会来我这里让我设计。 <br />
<br />
　　叶明子的童年有着和普通孩子不尽相同的经历，小时候的叶明子一直跟随父母和叶剑英元帅居住在香山脚下，在她的描述中，虽然童年的住处风景美不胜收，但由于地处偏僻，叶家居住的地方几乎没有什么邻居，亲戚也大多搬到北京城里居住。 <br />
<br />
　　“当时在一起玩比较多的是邓爷爷（邓小平）的外孙女，但也不是总能见面玩耍。”因为缺少玩伴，童年对于她来讲，是比较孤独的，直到7岁时举家来到香港之后，父母重新找工作，叶明子也开始念小学，她对真正习惯于与人交流，与朋友们一起玩耍的经历也是从那时开始的。 <br />
<br />
　　7岁之前，叶明子就有随爷爷的专机到处去，这给小叶明子一直留下的印象是――飞机就是两三个人坐的。对于那个年代，很多人连飞机场都没有去过，更别说是坐飞机去游玩。飞往香港的客机上，7岁的叶明子惊讶地看着机厢里的乘客，到这时她才知道，原来飞机也是可以这么多人一起坐的。 <br />
<br />
　　叶明子的母亲是画家，按她的话说，自己的艺术设计细胞是被母亲感染的。而小时候叶明子的很多衣服也都是妈妈为她亲手做的。另外，叶明子是个从小就懂得怎样以另类美吸引别人眼球的女孩儿。“那时和家人在一起的时候通常我打扮得和他们都不一样。十几岁的时候我的打扮就非常惹眼。而家里人也慢慢习惯和接受了这种打扮，有一次我把很黑的头发染成了紫色和藕荷色，回到家，家里人都目瞪口呆了。 <br />
<br />
　　对于叶明子来讲，童年的定制生活在她对服装的理解中体现得淋漓尽致――彻底的对衣服的每一根线进行设计以达到定制的效果也是她最初的愿望。叶明子在北京建立了一家私人工作室Studio Regal――Studio是工作室的意思，而Regal则有堂皇、华丽、皇室的概念。叶明子为工作室起这个名字的目的是为了彰显自己服装的华丽、神秘和高贵。她将自己的服装设计定位为“定制高级时装”，这个概念原先在欧洲的皇室贵族很流行。一些贵族通常会找一个设计师为自己设计衣服。也叫私人定制。 <br />
<br />
　　一般我设计一套服装的价格大概在几千到几万元之间不等，这些费用包括了设计和成品费用。”现在的叶明子打算将自己的业务扩大些，这样的定制服装只有在设计界和名人圈里出名才能更好地发展下去。“实际上明年我打算开一个发布会，请一些以前的顾客和一些圈子里的人，给他们看一些板，增加一些客户。我并不会很着急扩大规模，如果扩大了我也会请国内本土的设计师来帮助我。” <br />
