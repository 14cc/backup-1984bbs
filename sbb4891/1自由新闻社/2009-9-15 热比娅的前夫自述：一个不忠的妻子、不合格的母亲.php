													<h2>热比娅的前夫自述：一个不忠的妻子、不合格的母亲</h2>
						
						
																											<div id="postmessage_241750" class="t_msgfont"><a href="http://news.163.com/09/0728/19/5FB74K6N000120GR.html" target="_blank">http://news.163.com/09/0728/19/5FB74K6N000120GR.html</a><br />
<br />
　　编者按：7・5事件是以热比娅・卡德尔为首的世维会等境内外三股势力精心组织、策划的严重暴力犯罪事件，也让人们认清了热比娅・卡德尔--这个被境内外三股势力奉为维吾尔族人精神母亲的女人的真实面目。<br />
<br />
　　本网今天刊发热比娅的前夫阿不都热依木・托乎提的自述，阿不都热依木・托乎提和热比娅共同生活了15年，育有6个子女。通过他的自述，相信人们会从一个侧面对热比娅有更清楚的认识--一个连自己的丈夫、孩子、家庭都不爱的女人，怎么能配被称为维吾尔族人精神母亲？又怎么可能热爱自己的民族，热爱自己的祖国？谎言重复一千遍也不会变为真理，一个背叛民族、背叛祖国的人，终究会被钉在历史的耻辱柱上。<br />
<br />
　　热比娅・卡德尔是一个黑心的女人，绝不能让她的阴谋得逞<br />
<br />
　　我叫阿不都热依木・托乎提，是热比娅的前夫。<br />
<br />
　　1962年4月,我同热比娅结婚，1977年6月我同热比娅・卡德尔离了婚。我们共同生活了15年，生育了6个子女。热比娅・卡德尔真是一个黑心的女人，她给我和子女造成了很大的伤害。乌鲁木齐市7・5事件是她一手操纵的纯粹的恐怖行为、纯粹的危害民族的行为、纯粹的背叛国家的行为，我们绝不能让她的阴谋得逞。<br />
<br />
　　热比娅・卡德尔是一个不忠的妻子<br />
<br />
　　热比娅1946年出生于阿勒泰，她有一个姐姐叫左然木・卡德尔，从新疆学院（现在的新疆大学）毕业后分配到阿克苏师范学校（现在的温宿县一中）工作，后在温宿县成了家。<br />
<br />
　　热比娅・卡德尔的妈妈就带着她和她的妹妹、弟弟从阿勒泰投亲到温宿。当时，我在阿克苏地区银行工作（现在的县医院旁边），和她姐姐家距离很近。她们来到温宿后，由于没有房子，就到处找房子，我住的房子有两间空房。我看他们可怜，就让他们住了半年多时间。<br />
<br />
　　1962年4月份，在双方父母的撮合下，我和热比娅・卡德尔搞了一个简单的仪式成了家。阿克苏地区银行搬走后，我于1965年又调到温宿县银行任副行长。很早以前，我就对热比娅・卡德尔有看法。她后面找了一个男的叫司迪克・阿吉。不是一个好东西，是一个老牌的民族分裂分子。司迪克・阿吉娶得第一个老婆叫买合木沙，和热比娅・卡德尔的姐姐是同事，是阿克苏师范学校的教师。<br />
<br />
　　有一年，热比娅・卡德尔的第三个孩子出了麻疹，司迪克的娃娃也出了麻疹，两家的娃娃都在医院治疗。所以热比娅就和司迪克・阿吉认识了。我在托乎拉公社六小队支农时，有一天发生了一个奇怪的事情，我从托乎拉回到家里……。我们离婚两年后，热比娅・卡德尔又来找我复婚。我就直接对她说，你这样的烂女人，我不要你，你走吧！<br />
<br />
　　热比娅・卡德尔不是一个好母亲<br />
<br />
　　热比娅・卡德尔从64年生到76年4月份，总共生育了6个孩子。她有一个最大的坏毛病就是不做家务、不管孩子。家里乱七八糟，房子里放个东西也找不到，在家里不做饭，经常是早饭等到中午吃，下午饭等到晚上吃，该做的饭不做，我和孩子饿得实在受不了。<br />
<br />
　　我在托乎拉村六小队工作时，有一天她突然给我打电话，要求离婚，把我骂得一塌糊涂。孩子小，我不想离婚。<br />
<br />
　　离婚的时候，她把六个孩子都抛弃给了我，当时最大的孩子13岁，最小的孩子才三个月，我就把六个孩子带到托乎拉公社吃食堂，是共产党把我的六个孩子养大的。热比娅・卡德尔从来没有教育过子女，对孩子的教育根本不管，六个孩子都是我带大的。她不是一个合格的母亲，更不是维吾尔族人的精神母亲。<br />
<br />
　　热比娅・卡德尔是一个地道的民族分裂分子<br />
<br />
　　热比娅・卡德尔找第二个男人司迪克・阿吉，阿图什人，他不是一个好东西，是一个老牌的民族分裂分子，曾被判了刑，文化大革命时候外逃又被抓了回来。出狱后，被安排到新疆学院（现在的新疆大学）工作，后来又逃到美国。他的第一个老婆阿克苏师范学院的教师买合木沙得知消息后，就通过法院发通告，和他离了婚。<br />
<br />
　　热比娅・卡德尔离婚后，就和司迪克・阿吉结了婚。热比娅・卡德尔在南北疆到处做生意发财后，党和政府给了很大的荣誉，她要是老老实实地把生意做下去，把子女教育好，为国家做出贡献就好了。但最后却走上了民族分裂这条路，一手导演策划了乌鲁木齐7・5事件，这么一个纯粹的恐怖行为，一个纯粹的危害民族的行为，一个纯粹的背叛国家的行为。<br />
<br />
　　我家里有2台电视，我们天天看新闻，我们一定要脑子清醒，在大是大非面前决不能动摇，我们决不能让她们的行为得逞。热比娅・卡德尔做出这样的事情，我太伤心了，她真是一个黑心的女人。<br />
<br />
　　我是一名共产党员，不论是我们的共产党员还是我们的人民，遇到这样的情况，麻木不仁是不行的！这是我的旗帜，这个很重要！<br />
<br />
　　中国共产党是一个伟大正确的党，中国社会主义道路是世界上最好的道路。<br />
<br />
　　我出生在解放前，经历了新疆解放前和解放后两个阶段，1951年7月1日参加工作，共产党的干部和国民党的干部不一样，说话、办事、用人都不一样，国民党是打人，共产党是培养人。我就是共产党培养起来的少数民族干部。<br />
<br />
　　我初小高级班毕业后（相当于现在的高中一年级），我们班20多个人一起参加了工作。国民党时期，凌晨四五点钟就让我们起床干活，稍不注意就要挨打。<br />
<br />
　　解放后，从延安来了一个共产党的行长叫薛敬文，50多岁。当时，我们20多个十五六岁的娃娃调皮捣蛋，是他耐心地教育培养我们，把我们从小孩带成了大人，培养成了少数民族干部。<br />
<br />
　　1956年，薛敬文行长把我上报到地区银行，提拔我当了副行长，1965年我被上级表彰为学大寨模范个人，1979年我光荣地加入了中国共产党。<br />
<br />
　　中国共产党在培养少数民族干部上下了很大的功夫，是我们的恩人。六十年代的时候，热比娅・卡德尔没有工作，我一个人一个月的工资有70多块钱，要养活8口人，生活得很困难。要不是组织上给我们补贴，关心照顾我们，早就把家里人饿死了。<br />
<br />
　　解放后的60年，伟大的祖国发生了翻天覆地的变化，这些变化，谁要是看不到，闭着眼睛摸都摸得出来。现在我和我老婆两个人一个月有6000多块钱的退休工资，我们生活得很好。<br />
<br />
　　就是热比娅・卡德尔把我们搞得很伤心。我们中国的社会主义道路是最好的道路，我们的国家是最有希望最有前途的。不论热比娅・卡德尔搞什么样的分裂，最后还是要走中国的社会主义道路！热比娅・卡德尔的分裂破坏活动我们不愿意看到，注定要失败！<br />
<br />
　　我们中国的民族是最团结的民族。<br />
<br />
　　我们中国有56个民族，新疆有47个民族，各个民族团结得像一家人。我特别喜欢看汉语频道，喜欢说汉语，喜欢汉族朋友。为什么呢？1953年9月到1954年3月，我们56名新疆金融工作者参观团，走遍了中国除西南行政区的24个省区市，亲身感受了汉族同胞对少数民族同胞的关怀，我一辈子都忘不了。<br />
<br />
　　我们刚到大连时，一下车，一个汉族老人得知我们是新疆来的少数民族干部时，那个老人流着眼泪说：中国有56个民资，你们就是我们的亲兄弟。当时，一群汉族老大娘、老大哥激动地流着眼泪跟我们聊天，我一辈子也忘不了。<br />
<br />
　　我们在北京待了一个月，北京所有的公园都参观了。到了青岛海边一个大公园，后面有三四个车的武警保卫我们。白天，青岛市人民政府请我们吃饭，晚上安排我们看一个外国电影。看电影的人特别多，我们被安排在二楼前面最好的位置。有一名随团干部喝多了，吐在了一楼看电影的汉族人头上，可人家一句话也没有说我们，让我们继续看电影。到了上海，上海市的领导给我们作报告时，看到我病了，就把我送到医院住了8天院。同时还有一个民族同志得了炭疽病，也和我在一家医院住了8天院。<br />
<br />
　　医院里把我们两个新疆少数民族干部真是当成宝贝了，专门为我们两个从老远买来了羊肉，为我们两个单独做饭。我们出院时，其他54个同志已到了杭州。上海24个同志为我们两个人送行，并派了4名干部把我们护送到杭州。<br />
<br />
　　在长沙时，我不小心打坏了宾馆一个小茶碗，我就把三毛钱放在报纸上面当做赔偿。等我们到了武汉时，长沙的宾馆已经把三毛钱寄到武汉了。<br />
<br />
　　这几件事，我一生都难以忘记。我学习汉语也是从内地看到发展、感受到汉族同胞的关怀后，才下决心要学习的，我不仅汉语说得很好，还注意音调。我喜欢看汉语频道，特别喜欢看中央一台、中央四台和新闻频道。1953年，汉族同志送给我的汉文本子，几十年来我像宝贝一样珍藏着。我们中华民族是一个伟大的民族，反正有中国人的地方，什么样的奇迹都会出现！（完）（责任编辑：马敏）<br />
