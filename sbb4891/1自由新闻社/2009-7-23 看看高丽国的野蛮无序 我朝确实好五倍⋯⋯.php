<br />
韩联社首尔7月22日电 国会22日举行全体会议，通过了《报纸法》、《广播法》、《网络电视法》等“与媒体相关的三项法案”。<br />
<br />
传媒相关法于去年12月提交国会，这段时间在朝野间剧烈对峙的情况下通过职权提交，历时7个多月今天在国会得到通过。<br />
<br />
国会副议长、大国家党议员李允盛当天下午在主持召开全体会议，对《报纸法》、《广播法》、《网络电视法》等“与传媒相关的三项法案”，以及《金融控股公司法》进行了职权提交，并通过表决方式予以通过。<br />
<br />
随着《传媒法》得到通过，报纸和广播今后将可以“兼营”。报纸和大企业可以持有地面波电视台10%的股份，并参与经营甚至拥有。此外，还可以持有综合整编频道和专门报道频道30%的股份。<br />
<br />
如果报纸的阅读率（在所有家庭中阅读特定报纸的家庭所占比例）超过20%，禁止兼营地面波电视台或持有股份，但由于现在并没有阅读率超过20%的报纸，因此，《朝鲜日报》、《中央日报》及《东亚日报》等主要报纸可以进军广播。<br />
<br />
此外，报纸、大企业及新闻通讯社在转换为数字电视的2012年12月底前，禁止成为地面波电视台的最大出资者，或实际支配经营权。（完）</div>

							
							
							
							
