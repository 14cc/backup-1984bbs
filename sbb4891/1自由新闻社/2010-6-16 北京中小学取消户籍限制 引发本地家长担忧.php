													<h2>北京中小学取消户籍限制  引发本地家长担忧</h2>
						
						
																											<div id="postmessage_686556" class="t_msgfont"><a href="http://news.sina.com.cn/c/sd/2010-06-08/113420434429.shtml" target="_blank">http://news.sina.com.cn/c/sd/2010-06-08/113420434429.shtml</a><br />
<br />
北京中小学取消户籍限制引发本地家长担忧<br />
<br />
<a href="http://www.sina.com.cn" target="_blank">http://www.sina.com.cn</a>&nbsp;&nbsp;2010年06月08日11:34&nbsp;&nbsp;南风窗 <br />
<br />
　　北京教育：实现公平有多难？<br />
<br />
　　文_本刊记者&nbsp;&nbsp;田&nbsp;&nbsp;磊&nbsp;&nbsp;发自北京<br />
<br />
　　6月是中国的高考时节，每年这个季节也都是章冬翠最揪心的时候，从1985年开始，她举家从浙江温州农村搬来北京已经25年了，她自己早已适应了没有户口的北京生活，但她的女儿、15岁的丁璇却无法适应。<br />
<br />
　　从小学开始，丁璇已经在北京读了10年书，无论在哪个学校，一直都是各类考试的第一名，因为学业优秀，她的父母并没有让她像大部分外地同龄孩子那样中途辍学，而是选择了将读书进行到底，“其中经历的千辛万苦都已经走过来了，但现在面临高考的门槛，无论如何也迈不过去了。”章冬翠说。<br />
<br />
　　过去的5年，北京城里像丁璇这样的外地孩子越来越多，到现在已经有40万左右，早已成为政府不得不面对的一个实际问题。对外地人口子女入学的限制最关键的两个环节，一是“小升初”，一是高考。<br />
<br />
　　今年5月，北京市政府废除了1986年沿用至今的《中、小学学生学籍管理办法》，并规定非京籍享受同等入学待遇，并为其建立学籍。<br />
<br />
　　虽然教育部门同时也不忘强调，这样的待遇仅仅限于义务教育阶段，而不包括高中和高考，但毕竟北京教育向外地人开了一扇窄门。不过这项变革却引起了不少家里有孩子的北京市民相当强烈的反弹。<br />
<br />
　　有学籍就意味着外地人的孩子可以参加派位、推优、评选三好学生，可以堂而皇之地跟京籍学生一起竞争优质教育资源。在记者的采访中，不少北京家长惊呼“狼来了”，对这项改革的抱怨、对外地孩子的憎恶溢于言表。<br />
<br />
　　“我们已经被教育折磨得痛不欲生了。”一位姓孙的北京家长说，北京那点可怜的优质教育资源被权贵阶层垄断一部分，如果再被外地人抢走一部分，我们这些普通老百姓的孩子该如何生存？<br />
<br />
　　作为首善之区，北京拥有全中国最优质的教育资源，却也面临着从小学就开始的最残酷的竞争，权贵与平民、穷人与富人、本地人与外地人，人人都希望通过教育维系或者改变家族下一代的命运，教育层面的冲突和斗争也才刚刚开始。<br />
<br />
　　统考、派位与“共建生”<br />
<br />
　　1998年是北京基础教育的一个分水岭，在那之前，跟全国各地一样，北京的小学生要想升入初中，都需要经过统一的升学考试，但这样的统考制度，从1980年代开始，实行了将近20年后，已经让那个年代的小学生们苦不堪言。<br />
<br />
　　时任北京一中校长的王晋堂告诉记者：“那个时候，考试的题目一难再难，可是，语文数学两门课总共200分，大量孩子还是能够得到满分，以至于很多中学的分数线都划在了198分。”<br />
<br />
　　这样的高分数更多是小学生们死读书、背试题的结果，沉重的课业负担已经让小学教育异化到了不得不改的地步。1998年，北京市的教育部门终于下定决心，取消了小升初的统一考试，引入了一项名为“电脑派位”的入学机制，这项源自于西方主要是英国伦敦等大城市的教育制度被当时的学生家长称为“洋抓阄”。<br />
<br />
　　电脑派位以公平为导向，取代了统考制度下的以分数为导向来分配学位，在当时的教育环境下，可以说是一项激进的、充满着理想主义色彩的教育制度。但它在北京落地的第一天起，就遭遇了极大阻挠。<br />
<br />
　　“一开始，主要是两股力量反对。”王晋堂说，一是学生家长，另外就是当时的名校，即使被派去了比较差的学校，家长们也都会想尽一切办法将孩子的学籍调出来，再送到心目中的好学校，有钱的用钱，有权的用权。而学校则以各种理由，拒收派位生。<br />
<br />
　　对电脑派位最致命的破坏则源于共建学校制度。这是一项最具北京特色的招生模式，其基本内涵就是政府机关和企事业单位为了满足员工子女入学，通过单位赞助钱或物的方式与知名中学建立“共建”关系，从而使双方“互惠互利”，单位员工子女可以轻松享受极为紧缺的优质教育资源。<br />
<br />
　　军队和外交部的共建校是较早的，后来扩展到各大中央部委，再后来，北京市级机关、大型国有垄断性企业，甚至是实力雄厚的民企，都有了自己的共建校。据北京媒体报道，到今天，房地产商、中小企业，甚至街道、派出所等也与学校建立了共建关系。不同单位与学校的共建门槛有所不同，比如共建费用问题，每个学生不同单位之间相差十多万乃至几十万元。<br />
<br />
　　在电脑派位之外，共建制度给特权留了个后门，而且越来越大，越走越顺利，到今天，这已经成为北京子女入学最省钱、省力又便捷的途径。共建生挤占了很大一部分优质的教育资源，尤其是在那些知名的中学，有的学校共建单位从一开始的一两个发展到后来的十几个。<br />
<br />
　　“共建生”对电脑派位规则的破坏远不仅仅是挤占名额，哪个学校的共建单位多，共建单位级别高，越是能吸引更多家长们趋之若鹜，希望把自己的孩子送进去，从而也让学校教育质量的差距也越来越大。这样的恶性循环让北京基础教育离均衡化越来越远。<br />
<br />
　　在特权面前规则总是不堪一击。10多年下来，电脑派位这个充满着公平精神的入学规则，早已名存实亡。据家长们统计，全北京不接收派位生的学校已经达到20多所。<br />
<br />
　　条子、票子和孩子<br />
<br />
　　共建生为各个名校带来了大量的资金、社会资源，但并不能带来优质的生源。学校的管理者很清楚，长此以往名校也可能会变成烂校。因此，最近3年，又诞生了另外一项制度――推优。主要是由各个小学根据平时成绩推荐优秀学生，但成绩并不是唯一标准，除此之外，三好学生、各类艺术特长都是录取依据之一。<br />
<br />
　　在王晋堂看来，这一制度的初衷就是为了保护优质教育资源，因为当时取消了市重点、区重点等划分标准，必须出台一种政策来保护原来的重点校，继续维持教育的不均衡状态。<br />
<br />
　　事实上，推优原本就是电脑派位制度的配套制度之一。中国各地的电脑派位制度多是借鉴广州海珠区1990年代的试验，北京也不例外，广州的派位制度则直接照搬了当年香港的制度，而香港则基本沿用了英国大城市的入学模式。<br />
<br />
　　电脑派位虽然保证了公平，但却不可避免地会抹杀一些天赋出众的孩子。在英国最初制度设计者的思路中，这些是被考虑在内的，因此，电脑派位的同时也辅以择优选拔，以完成普遍公平与成才效率的有机结合。<br />
<br />
　　在西方国家，择优选拔权大多都是直接赋予各校校长。名校的校长是享有崇高声誉的社会职位，人们相信，如果他滥用权力，名校很快就会变为烂校，而且，大多数公民不相信一个声名狼藉的校长能治理好学校，并给予自己的孩子最优秀的教育。因此，校长的择优选拔权在一定程度上保持了纯洁。<br />
<br />
　　在中国一切都变得不一样了，以北京为例，名校校长的品格或许并不足够可信，他也没有足够的能力来抵挡权力和金钱压迫，但这并不影响他的学校独占鳌头，因为那里边集中了权贵阶层的孩子，在每次的政策修订中，也总会有更多的话语权，政策无论如何改变，总会起到强化自身利益的作用，保证更好的老师、更好的设备和更好的学生。<br />
<br />
　　与电脑派位一样，推优制度也遭遇了严重异化。最近一周，正在进行的如火如荼的特长生考试就是最典型的结果，为了让孩子们在升学时增加砝码，从小学一年级开始，各类以推优为目标的特长培养就开始了。<br />
<br />
　　但无论如何，对于那些无权无势的普通市民家庭而言，推优毕竟是一条进入名校的小路。一位孙姓的北京家长，在网上建立了一个QQ群，命名为“无权不牛小升初群”，吸引了无数的家长参与，他们建有自己的论坛，每天围在论坛上讨论小孩考初中的门路。<br />
<br />
　　他用了这样几句话描述自己孩子的生活：每天回家一小时作业，一小时乐器，一小时奥数，一小时英语，加上磨蹭时间，基本每天晚上11点半睡觉，咖啡和茶水是生活必需品。总结一下，我们是家长没权，孩子没才。他的自述引来了无数同病相怜的家长们唏嘘不已。<br />
<br />
　　“孩子吹萨克斯吹得嘴都变型了。”这位家长说，我自己都痛恨自己的教育方式，可是，转念一想，再痛苦也就痛苦这几年，如果放任不管，孩子只能进入烂校上学，痛苦的将是一辈子。<br />
<br />
　　在北京各类教育论坛上，这个群体都是最活跃的，他们都是普通的北京市民，自认为属于中产阶级，无权无势，重视教育，将改变家族命运的希望寄托在孩子身上，而改变命运的第一步就是进入名校。“有权的用条子、有钱的用票子，像我们这样的小市民，只能是拼孩子了。”<br />
<br />
　　到今天，这些北京城里的中产阶级家庭早已习惯了特权林立的教育现状，在夹缝中为自己的孩子开拓空间，付出了巨大成本之后，他们反倒成了这种不公正的教育制度最坚定的拥护者。<br />
<br />
　　2009年，新的教育部长袁贵仁上台，高调提出了教育均衡化，不仅大力打击择校风，更要求各地逐步向外地孩子开放义务教育，这些被媒体呼唤了多年、显然更符合大多数人利益的教育诉求，遭遇的阻力却不仅来源于特权阶层，更来源于普通的市民家庭。<br />
<br />
　　“教育部长说要搞均衡化，可为什么不先把教育部的共建校取消了？领导的孩子为什么不接受派位而都是往好学校挤？”那位孙姓家长认定，教育均衡化只是一句骗小老百姓的空话。<br />
<br />
　　在他看来，自己的孩子付出了这么多，却要跟那些毫不重视教育的家庭一样，接受电脑派位，学得好不如运气好，这是一种多么大的不公。更严重的是，现在居然那些外地孩子也想跟我们一起来抢夺资源。<br />
<br />
　　他们抱怨特权，却又无可奈何，只能将所有的憎恶集中于外地孩子身上。<br />
<br />
　　校门外的孩子<br />
<br />
　　丁璇就属于那个被憎恶的群体。2000年，不满6岁的她，被常年在北京打工的父母带到了北京。她的母亲章冬翠凑足了5000块钱借读费，为她找到了一所小学，这是母亲做小时工、父亲拉人力车攒下的血汗钱。<br />
<br />
　　在备齐了暂住证、劳动合同、派出所证明等七项证明后，丁璇终于跨进了北京小学的大门。等到升初中时，章冬翠求爷爷告奶奶，跑了几十家学校，缴齐了各类名目繁多的费用后，最终找到了一所区内较差的学校，丁璇得以留在北京继续读书。<br />
<br />
　　父母的付出得到了些许回报，在过去的10年里，丁璇几乎取得了现行的教育体制下，她能够获得的一切荣誉。她是班长、团支书，学习成绩一直都是全年级第一名，市、区、校三级三好学生，奥数、英语口语比赛得过一等奖，书法、绘画、朗诵、作文比赛都拿奖无数，当过奥运志愿者，刚读高一，就得到了上党校学习班的机会。<br />
<br />
　　5月底，记者在胡同里找到他们租住的那所阴暗潮湿的旧房子时，她和她的妈妈正兴奋于取得的一项新成就：她成为全校仅有的两名东城区第九次团代会代表之一。<br />
<br />
　　这个15岁的小女孩，并不能完全理解她所参加的每一项活动的价值。她只是用各种办法试图证明自己并不比北京孩子差。10年下来，除了数不清的荣誉，她记得最清楚的另外一件事是，初三的时候，全班评选三好学生，她被同学们选上了，但老师却取消了她的三好学生名额，理由是反正你没有学籍、不能参加推优、不能读好中学，这些荣誉给你就是浪费了。<br />
<br />
　　那一次章冬翠大发雷霆，跑到学校跟老师理论，“三好学生评的是孩子的品质，不是户口，而且教委有文件外地生也可以评三好。”最终，老师将本属于她的三好学生还给了她。<br />
<br />
　　这让丁璇意识到，无论如何优秀，自己都不属于这个城市。初中到第三年时，就开始不断有老师找她谈话，劝她退学，希望她不要参加中考。<br />
<br />
　　初三时的劝退行为，是各个初中针对外地孩子一项普遍的做法，一旦这些孩子在北京参加了中考，并大量涌入高中后，挤占优质学位、降低北京的高考录取率仅仅是一个小问题，更严重的是，在3年之后，当整个城市出现数以万计的高中生无法参加考试的局面时，将会成为教育部门一个难以回避的尴尬局面。<br />
<br />
　　最终，当初跟丁璇一起在同一所初中上学的75个孩子，大部分都被劝退了，但章冬翠是个认死理的人，“坚决不退学，不管怎样也不能把孩子毁了。”<br />
<br />
　　丁璇的故乡是浙江温州农村，但她早已回不去了，语言不通，从小学开始的教材、考试方式、科目全都不一样，这些都让她不得不硬着头皮留在北京读高中。<br />
<br />
　　最终，她在北京参加了中考考试，满分570，在不算其他加分的情况下，她得了527分，这个分数足以让她上北京最好的高中了，如果她有北京户口的话。<br />
<br />
　　2009年，为了女儿进高中，章冬翠又开始了奔波，可这一次，她实在无能为力了，跑了几十家高中，赞助费最少都是4万以上，这远远超出了她这个一贫如洗的家庭的承受范围。但幸运的是，她碰到了一个好校长。<br />
<br />
　　“我找到22中时，碰到了一个好校长。”章冬翠说，22中的校长是个开明的人，看到丁璇这么优秀，一口就答应收了下来，仅仅按照教委政策，收了1.2万的借读费，其他全免了，全年级只有两个外地生。<br />
<br />
　　丁璇如履薄冰的求学路又一次得到了延续，但3年后的高考该怎么办？横亘在面前的高考制度是她再也跨不过去的门槛，纵是倔强的母亲、好心的校长也无能为力。<br />
<br />
　　章冬翠也开始泄气了，“偌大的中国，再也没办法为女儿找到一个参加高考的地方。”她也没有能力像那些有钱的外地人一样，直接将孩子送到国外去读大学。<br />
<br />
　　“不能改的高考”<br />
<br />
　　5月份北京市政府废除了1986年版的《中、小学学生学籍管理办法》，随后各区也陆续表态将会把非京籍学生也纳入电脑派位范围，这样的举措无疑将会促进越来越多的外地子女进入北京公立学校接受义务教育。但随之而来的问题是，这些孩子在北京完成了义务教育之后，该怎么办？<br />
<br />
　　由于全国各地教材改革、高考改革的进程千差万别，义务教育完成后，他们无法回到原籍参加高考，如果北京也不为他们打开高考之门，那么丁璇式的困境将会成为越来越普遍的问题，最终酿成巨大的社会矛盾。<br />
<br />
　　但开放高考，则意味着从根本上动摇了北京在教育领域享受特权的基础，在绝大多数北京市民以及官方眼里，这显然是一个不可想象的恶果。<br />
<br />
　　作为北京市政协教文卫体委员会的特邀委员，一直在各种场合疾呼教育公平的王晋堂也认为，高考是万万不能放开的，“那样，外地人就会蜂拥而至，北京的教育优势将不复存在，教育资源也会严重不足。”<br />
<br />
　　迄今为止，没有一项科学的调查报告，对那些外地人的教育意向做过考察，但这种担心却根深蒂固。在我们这样一个极端重视教育的民族，优质的教育资源产生的吸引力会大到什么程度？没有人能够断言。人们都愿意相信，那些外地人就是只为了让孩子上学，也一定会举家老小都往北京迁。<br />
<br />
　　但在著名律师许志永看来，这显然是想当然的结果，“短期内或许会对北京教育秩序造成一定冲击，但长远来看，当北京的高考录取率不再像现在这样奇高的时候，问题就解决了。”他认为在高考招生时，完全可以逐步实现按照报考人数来决定录取率。<br />
<br />
　　事实上，根据教育部门的统计，现在在北京的流动人口适龄入学子女有40万左右，这才是最大的威胁。一旦把所有限制都取消了，这样一大批学生将会迅速占据大量学位，老北京人在高考体制下享受了数十年的高录取率将不复存在。<br />
<br />
　　所以，近几年，随着北京户籍学生人数的减少，北京市教育系统宁可合并、减少中学数量，从而保证北京市高考的高入学率，也不愿意招收外地学生。<br />
<br />
　　可以说，就北京教育而言，不合理的高考制度是阻挠教育迈向公平最根本的原因。但改革高考显然还没有被提上日程，也许只有当它酿成重大社会危机时，才会被迫改变。<br />
<br />
　　不过，让人沮丧的不是制度进步的艰难，而是歧视与偏见根植于每个人的心中，在记者接触的各类家长中，权贵歧视平民，不愿意让他们的孩子跟普通人的孩子混在一个学校，普通市民歧视外地人，不愿让自己的孩子与外地人为伍，有钱的外地人歧视那些引车贩浆人的后代，害怕他们把自己孩子带坏。<br />
<br />
　　这样的教育会为我们的民族带来一个怎样的未来？</div>

							
							
							
							
