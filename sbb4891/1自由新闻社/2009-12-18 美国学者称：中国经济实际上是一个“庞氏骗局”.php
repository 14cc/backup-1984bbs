													<h2>美国学者称：中国经济实际上是一个“庞氏骗局”</h2>
						
						
																											<div id="postmessage_380347" class="t_msgfont">最新出版的美国权威财经杂志《福布斯》12月号刊登封面文章，题目是《中国的泡沫》，对中国经济过热提出警告。一些中国问题观察家也对中国房地产市场非理性繁荣表示关切。<br />
<br />
定于12月28号出版的美国《福布斯》杂志在封面文章《中国的泡沫》中说：中国的经济引起全世界的羡慕。在发达国家竭尽全力才能勉强维持微薄的经济增长并试图摆脱两位数失业率地同时，中国的GDP以8％的年增长率向前飞奔。这个拥有四万七亿美元的经济体似乎是世界发电机，并被塑造成为未来的原型。 <br />
在全世界金融和经济一片愁云惨雾中，只有中国经济似乎一枝独秀。一些中国左派甚至在互联网上发表文章，声称全球金融危机证明，西方自由市场经济和民主体制行不同，只有&quot;中国模式&quot; 才是世界未来发展的模式。<br />
<br />
前经济学周刊杂志副总编，著名新闻人高瑜认为，所谓中国模式，不能持久。她说：“ 中国模式是任何国家都无法仿效的，因为它用了毛泽东留下来的一个人口红利。低人权，低工资，低福利，这样的人口攒下的红利，才使得中国经济腾飞。而且这样的红利今后也没有了。”<br />
<br />
<strong>日本的前车之鉴</strong><br />
<br />
《福布斯》杂志也给弥漫在中国的一枝独秀的过度乐观情绪吹冷风。福布斯杂志的文章说，仔细分析，你可能得出的结论，今天的中国经济和20年前股票市场和房地产市场崩盘的日本非常相似。中国出现投机性借贷和投标热潮，如果价格暴跌，将出现无法支付的地狱。 <br />
<br />
<strong>肥水不流别人田</strong><br />
<br />
《福布斯》说，中国政府官僚机构通过向国营企业贷款来为自己提供资金；地方政府出售天价土地给地方拥有的公司来筹集资本； 中国人民银行在整个系统的流动性充足，使得美国联邦储备委员会主席伯南克显得像个吝啬鬼。 <br />
<br />
<strong>庞氏骗局</strong><br />
<br />
福布斯杂志援引美国西北大学的中国问题专家维克托．施的话说，<strong>中国经济实际上是“一个庞氏骗局，主导的是中国的央行，而且它还可以印钞票。”</strong><br />
<br />
像美国几年前的房地产市场一样，中国房地产的大发展依赖的也是低利率和房价上升。地方政府深陷房地产资产的沼泽，无法自拔。<br />
<br />
<strong>怎一个“热”字了得</strong><br />
<br />
随着中国的城市化，很多大城市不断推出更多的写字楼和豪华商场用来出租。离北京不远的天津现在可以提供的优质写字楼空间，以目前的吸收速度，大概要四分之一个世纪才能填满。顺义县在首都郊区，上个月出售的住宅地价为每平方英尺400美元，创下新的全国纪录。投标的大多是国有企业，而标王则不是别人，而是顺义县拥有的开发商。<br />
<br />
福布斯杂志指出，中国的贸易政策是资产泡沫的另一个来源。通过人为压低其货币价值从而使得企业很难到国外投资，中国人为地迫使资本大量追逐国内投资，虚报房地产和股票价格。这和中国2007年是同样的情况。2007年底中国的股市跌了市场价值的三分之二，而和今天相比，当时的那个时代实行的是货币紧缩的政策。 <br />
<br />
福布斯杂志援引中国经济学家谢国忠的话说，<strong>“这纯粹是在进行一场债务的比赛，” 谢国忠认为目前的泡沫“比以往更大。” </strong><br />
<br />
<strong>见仁见智</strong><br />
<br />
11月下旬，中国执政的政治局宣布国家的货币和财政宽松政策将持续到2010年。可以想象，中国市场的反应是欣喜若狂；而外国经济学家则看到十几年前俄罗斯和巴西金融危机前曾出现过的同样乐观情景。<br />
<br />
<strong>泡沫长寿说</strong><br />
<br />
尽管章家敦等人作出的&lt;&lt;中国即将崩溃&gt;&gt;的预言没有实现，但是福布斯杂志指出，泡沫有一个值得一提的特点，那就是它的长寿出乎许多观察家的意外。 福布斯就曾经在六年前的封面文章中过早地警告过美国房市的泡沫威胁经济。不过，当泡沫最后破裂的时候通常会伴随着一声巨响。 <br />
<br />
就像在经济高速增长的晚期出现的典型案例那样，今天中国的许多投资者似乎已经不再把租金收益作为投资房地产考虑的因素。他们追求更大的不可理喻的价值。在北京的市中心，办公楼的价格为每平方英尺400美元，尽管有大量写字楼根本没有租出去，但是，更多的写字楼正在紧锣密鼓地建设之中。其领先者是中国的国有企业，包括银行和保险公司。 <br />
<br />
《华尔街日报》的一篇文章也对2009年中国房地产市场的逆势繁荣感到困惑和担忧。<br />
<br />
<strong>疯狂大抢购</strong><br />
<br />
在一篇题为《中国房地产市场深陷非理性繁荣》的文章中，华尔街日报指出，2003年以来，关于中国房地产泡沫即将崩溃的言论就层出不穷，但房价一直扶摇直上，只是在2008年放慢了脚步，进入2009年再次直冲云霄，部分城市房价已超过最火爆的2007年水平，许多拥有多套住房的人从穷人一跃成为富人，就像中国股市建立初期深圳流行的一个口号一样，“不买股票，你就是深圳的最后一个穷人”目前是谁再不买房子，谁就可能沦为中国最后一个穷人。<br />
<br />
<strong>坏故事的警醒</strong><br />
<br />
《华尔街日报》援引耶鲁大学著名金融学教授罗伯特．席勒的话说，&nbsp;&nbsp;中国的房地产市场似乎缺乏一个像美国大萧条一样的坏“故事”。无论是普通购房人、投机者、房地产商和各级政府都沉迷于好故事的情节里，美国、日本和中国香港等地区房地产泡沫的破裂都不足以构成警示。中国房地产市场需要坏故事的警醒，否则非理性繁荣持续得越久其破坏力就会越大。<br />
<br />
<strong>专家认为，是泡沫终究会破灭，但它往往比你预想得坚持更久。所以破不破从来不是问题，真正的问题是什么时候破。 </strong></div>

							
							
							
							
