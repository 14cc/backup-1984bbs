<br />
　陈良宇的房内设施只有一张离地面约1尺高的矮床，需要写东西时，狱方会送进一张小学生用的单人课桌椅供临时使用，没有凳子，床铺就是他平日坐的地方，墙壁也经过特殊处理，以防止他自杀。 <br />
