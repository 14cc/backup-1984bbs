　中央社30日报导，秦城监狱位置在北京市昌平区小汤山镇附近，号称「中国第一监狱」，1990年代后主要关押对象是正、副省部级以上官员。「羊城晚报」统计显示，近10年来被查处的高官超过100人。 <br />
<br />
