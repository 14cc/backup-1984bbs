													<h2>党媒报道秦城监狱现状 披露陈希同处于直肠癌晚期</h2>
						
						
																											<div id="postmessage_404158" class="t_msgfont">贪腐高官的服刑地<br />
<br />
　　《环球人物》杂志特约记者&nbsp;&nbsp;章功<br />
<br />
　　关押和改造贪腐高官，成为新时期秦城监狱的主要职责。<br />
<br />
　　在我国，对普通犯人执行刑罚，无论其被判处有期徒刑、无期徒刑，还是死缓，一般是就近执行，即在哪里判决，就在哪里执行。而对于省部级贪腐官员(含副省部级)，不论他(她)在哪里被判决，大多会被集中到秦城监狱来服刑。<br />
<br />
　　不是什么人都能进秦城监狱<br />
<br />
　　谁是改革开放后第一位被送进秦城监狱的贪腐高官，外界无从考证。但统计资料显示，近10年来被查处的副省(部)级以上高官超过100人。其中，除8人被执行死刑外，被判死缓的占11%，无期徒刑者占8%，有期徒刑10年以上者占21%，有期徒刑10年及10年以下者占15%。他们当中，绝大部分人被关押在秦城监狱，或在此服过刑。如原中共中央政治局委员、上海市委书记陈良宇，原国土资源部部长田凤山，原云南省委副书记、省长李嘉廷，原贵州省委书记刘方仁，原公安部副部长李纪周，原国家统计局局长邱晓华，原建行董事长张恩照，原北京市副市长刘志华，等等。<br />
<br />
　　在秦城监狱，特殊囚犯的生活待遇，会比在普通监狱优越。<br />
<br />
　　据近年走进或接触过秦城监狱的有关人士描述，秦城监狱关押高官的牢房除了面积较大，有的还配有写字台、卫生间、坐式马桶和洗衣机。据《凤凰周刊》披露，一些在押官员除了“可看书读报”，每天还有一段时间可看电视，一般集中在晚上7点到9点。某些身体欠佳的特殊囚犯，饮食可一日四餐，用餐标准和费用由国家规定和支付，家属亦可私下打理。如衣服、日用品等基本生活用品可由家人提供。监狱虽有统一囚服，但这里的囚犯一般可不用穿。<br />
<br />
　　除了贪官，近些年秦城监狱还关进一些其他的“特殊”罪犯，如危害国家安全罪犯、外籍犯、知密犯、国际间谍等。社会上曾广泛流传：一些犯法的文艺界知名人物也进过秦城监狱，如央视前文艺部主任赵安、知名词作家张俊以和著名演员刘晓庆等人。但据环球人物杂志记者调查，这些人实际上并没有进过秦城监狱。<br />
<br />
　　2002年，北京市公安局的看守所因监舍改造，将一批重要犯罪嫌疑人转往秦城监狱下属的第一看守所内，其中就包括当时因涉嫌偷税漏税在押的刘晓庆。张俊以等人，在出狱后还声称“在监狱放风时曾见过刘晓庆”，这说明他也不是被关在秦城监狱。至于刘晓庆后来自己说“在秦城监狱如何如何”，一位专家说：“那不过是借秦城监狱之名抬高身价罢了，秦城监狱不是什么人都能进的。”<br />
<br />
　　陈希同狱里狱外忙治病<br />
<br />
　　正是秦城监狱的特殊，使贪腐高官们一旦进入高墙内，他们的信息也就进入不透明阶段，其服刑情况和生活情况，公众都无从得知，只有一些零星的报道见诸报端。<br />
<br />
　　据《新周报》等媒体报道，陈希同是“四人帮”之后被监禁的最高级别的官员之一。他曾历任北京市长、中共北京市委书记、国务委员和中央政治局委员。1998年2月27日，陈希同因侵吞贵重物品、腐化堕落、牟取非法利益、严重失职等问题被逮捕。1998年7月31日，经北京市高级人民法院审判，陈希同以贪污罪及玩忽职守罪被判处有期徒刑16年。同年8月20日，最高人民法院终审判定，驳回陈希同的上诉，维持原判。<br />
<br />
　　陈希同案当年曾轰动一时，但此后不久就淡出公众视野。<br />
<br />
　　陈希同在1995年引咎辞职时，曾被“双规”居住在北戴河，后转往内蒙古呼和浩特监狱，再转至北京良乡，正式宣判后被送入秦城监狱服刑。陈希同的刑期是从被逮捕之日起计算的，按原刑期需要到2014年2月才能获释。<br />
<br />
　　在监狱里，有时候因为自己的要求被看管人员拒绝，陈希同会大吼大叫，称自己是“北京的大贪污犯老陈”。<br />
<br />
　　2004年，陈希同曾因健康问题，被紧急送往北京复兴医院。这次突然发病后，陈希同向相关部门领导写信，以“心跳异常、心血管供血不足等老年疾病”为由，要求“保外就医”。后经协调，自2006年8月下旬起，陈希同获得有条件的保外就医。<br />
<br />
　　2007年6月，有关人士透露，陈希同曾与人在北京某高级饭店共进午餐。<br />
<br />
　　据该人士描述，当时陈希同气色看上去还好，头发有些花白，走路需要人扶。在座者不敢问及他在监狱里的情况，但陈希同自己谈到了一些：监狱的条件也没有什么不好的，能单独做些事情；24小时受监控，但后来慢慢习惯了；还可下棋、打网球，这是他多年的习惯，监狱会派小战士陪着打。<br />
<br />
　　保外就医期间，陈希同主要住在北京郊区的一个小四合院里，经过批准可以见到一些老朋友。从2008年开始，陈希同病情趋重，就不再见外人了，主要在家养病，有时会到医院就治。但据说，陈希同在住院单上用的是化名。<br />
<br />
　　自今年4月中旬开始，79岁的陈希同，在住进北京医院高干病房救治后，院方已多次发出病危通知。陈希同已是直肠癌晚期，还有并发的心脏病，基本只能依靠药物维持生命。<br />
