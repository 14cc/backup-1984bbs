<br />
　他们当中绝大部分人被关押在秦城监狱，或在此服过刑，如陈良宇，前国土资源部部长田凤山，前云南省委副书记、省长李嘉廷，前贵州省委书记刘方仁，前公安部副部长李纪周，前国家统计局局长邱晓华，前中国建设银行董事长张恩照、前北京市副市长刘志华等。 <br />
<br />
