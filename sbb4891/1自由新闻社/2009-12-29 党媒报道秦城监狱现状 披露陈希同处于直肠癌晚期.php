王鹏捷/综合报导 <br />
<br />
　北京市秦城监狱是关押中国最高级别犯人的地方，里面待遇自然不同，可以看书读报看电视，日用品可由家人提供，还可以不用穿囚服，前上海市委书记陈良宇在狱中就是穿着西装。 <br />
