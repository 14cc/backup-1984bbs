													<h2>太祖的悲哀</h2>
						
						
																											<div id="postmessage_178683" class="t_msgfont">毛泽东主席的后代都很低调，尤其是其孙辈们更是很少以“特殊身份”出现在公众面前。毛泽东唯一外孙孔继宁就是这样。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;出生于1962年的孔继宁是毛家的外孙辈中唯一与毛泽东有过接触的人。尽管接触不多，但外祖父对他的人生仍然产生了极大的影响。孔继宁出生在中南海。因为他的生日与苏联“十月革命”俄历纪念日仅相隔两天，爷爷孔从洲将军和外公毛泽东共同为他取名“继宁”，意为继承列宁的事业。当时，毛泽东高兴地说：“继宁的出世让70岁的我‘官’升祖父级。”<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;人到中年的孔继宁依然英俊潇洒，他点烟的神态也酷似毛泽东，这让我们对开国伟人又多了一份不尽的思念。孔继宁没有走向政界。长大后孔继宁参军了，随后进入中国人民解放军南京国际关系学院英语系学习。毕业后到解放军总参谋部任职，先后被派往中国驻巴基斯坦使馆和中国驻英国使馆任武官助理。他说：“1997年，因为母亲身边缺人照顾，我才转业回北京。”<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;转业后，孔继宁开始亮相媒体，但频率并不高。在纪念红军长征70周年时，他推出了影响很大的《我的长征――重走长征路》，在纪念建军80周年之际，孔继宁主持了央视开播的纪录片《父辈的战争岁月》，人们对他的评价是主持风格亲和自然。主持这两个大型电视节目，我们可以看出，孔继宁是在以另一种形式诠释他对祖辈革命生涯的纪念，表达红色后代对革命事业的热诚之情。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;<br />
<img src="http://blog.people.com.cn/blog/blogspace/2007/0130/wgwyzy/200907200210540086.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;毛泽东长外孙孔继宁<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 对于全国人民爱戴的领袖，孔继宁曾谈过两点颇有意义的看法。他曾说：“现在很多人都很浮躁，喜欢人云亦云。比如我看到有人发牢骚说：‘毛泽东那个时候搞得那么贫困，看我们今天的生活多好！’我就想对他们说：我们吃到第五个馒头的时候就觉得自己饱了，但你不能说功劳都在那第五个馒头上，那第一个馒头没垫底吗？客观、负责地说，当时以毛泽东为首的这一批革命者，在特定历史条件下，完成了他们的历史使命。我们不能要求前人把所有的事情都做完；他们都做完了，后人干什么？很多东西要历史地去看待，读懂、读透历史背景才可以评价。”作为领袖的后代，孔继宁的认识是公正而充满深情的。这对那些有意无意不尊重历史的人来说，是一种警示！<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 孔继宁还感慨地告诉我们：“我尤其感慨主席晚年的生活，主席最悲哀的一点在于，他一生戎马，为国为民费尽心血，但自己却很少真正享受到温馨的家庭生活。”<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 作为毛泽东的外孙，孔继宁用“悲哀”一词形容主席的心路历程，很让人感慨万千。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 我们都知道毛泽东一门英烈，以他最亲密的战友杨开慧和最贴心的儿子毛岸英为杰出代表，他的七个亲人为革命献出了宝贵生命。除此以外，毛泽东曾为革命失去过7个儿女。这7个儿女中，毛岸英牺牲在朝鲜战场，而其他6个都是在年幼时就离散或夭折了，而这6个儿女中，除了毛岸龙是他和杨开慧所生外，其他5个都是他和贺子珍的儿女。1929年女儿毛金花是在出生半个月时就给老乡抚养了，据说一年后就离开了人世。1930年，毛泽东和贺子珍的第二个孩子出生，可一生下来就夭折了，连个名字都没有留下。1932年出生的儿子毛岸红（小名毛毛），是毛泽东最喜爱的孩子之一，他曾说，人家叫我老毛，我的儿子叫小毛毛，比我多个毛，将来要比我强哦！可是，两年后，红军被迫长征，毛毛不得不留在苏区，毛泽东甚至没能看上毛毛一眼，毛毛就被秘密安置了。1935年，红军二渡赤水时，他们的又一个女儿出生，由于战事激烈，过了赤水河就送给老乡了，也没有留下名字。他们的最后一个孩子出生在苏联，名字叫廖瓦，十个月时就染上肺炎去世了。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 在中国革命史上，确实没有人再像毛泽东那样为革命献出了如此巨大的家庭牺牲了。人非草木，孰能无情！伟人更是如此。毛泽东一生献给革命事业，而在晚年却无法享用家庭生活的美好，这是何等的悲情！<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 毛泽东的骨肉情让我们无不动容。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 由于特殊的历史原因，毛泽东病重时很难享受到家庭的温暖。人民网曾经发表过一篇回忆文章，题目是《“你要常来看我”――毛泽东与长女李敏》，毛泽东与长女李敏的最后一次见面，充满了人生的无奈，更充满了人性的关爱：<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;李敏第三次见到爸爸，是在她看到中央一个文件中谈到毛泽东病情以后。她不顾一切，立刻到中南海门口求见。当她见到了父亲的时候，毛泽东仰卧在床上，疾病折磨得他脸容憔悴声音微弱。但神智却十分清醒。他见到李敏站在床前，拉住了李敏的手，说：“娇娇你来看我了？”<br />
<br />
　　李敏点了点头。<br />
<br />
　　“你为什么不常来看我呢？”<br />
<br />
　　李敏无言以对，她知道，爸爸的心正像妈妈所说的，是很孤独、很寂寞的，他希望得到爱，享受和普通人一样的天伦之乐。<br />
<br />
　　“你今年多大了？”毛泽东接着问道。<br />
<br />
　　“三十九了。”<br />
<br />
　　“不，你三十八。”<br />
<br />
　　毛泽东重病在身，还清楚地记得女儿出生的年份，这父爱之情深深地打动李敏，她心头一颤，热泪盈眶。她竭力控制自己，不使眼泪掉下来，惹爸爸伤心。<br />
<br />
　　毛泽东艰难地打起手势。他用右手的拇指和食指连成一个圆圈，说了一句话。<br />
<br />
　　李敏没有听清，她用眼睛询问旁边的服务员，听到我爸爸说什么？服务员摇了摇头，表示他也没有听清。李敏也就没有回答。<br />
<br />
　　事隔多年，有一次李敏和朋友琢磨起这个手势，才明白那是父亲用来向她打听母亲情况的表示，因为母亲曾用过“桂圆”的名字。为此，她心情极其难过，感到了一种无可挽回的遗憾。<br />
<br />
　　当时，毛泽东看到李敏没有明白自己的意思，没有回答，不再说话，无力地闭上眼睛，李敏就退了出来。<br />
<br />
　　这就是李敏同毛泽东的最后一次会见。<br />
<br />
&nbsp; &nbsp; 读着这段令人震撼的文字，我们感受到的是伟人的平民情怀，更感受到伟人崇高的人格力量！为了中国革命的事业，为了人民大众的幸福，他牺牲的是个人的幸福，牺牲的是家庭的温暖，怎能不让人肃然起敬？<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp; 毛泽东的儿女情怀，让我们领略到什么是真正的人生，什么是真正的人性美。青年时的毛泽东也曾经拥有过年轻人的浪漫情思，他在结婚前写给杨开慧的词《虞美人》，是那么的柔情似水：<br />
<br />
　　 堆来枕上愁何状，江海翻波浪。<br />
<br />
