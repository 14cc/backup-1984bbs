胡舒立团队接手《新世纪周刊》<br />
<br />
本周一，业内即有说法称胡舒立的团队完成了对于《新世纪周刊》的入主，但确切的消息在昨日才水落石出。昨日下午，胡舒立团队的财新传媒发出了一条名为《胡舒立、杨大明、王烁出任新世纪周刊编辑部高管》的公告，公告称已聘任胡舒立担任《新世纪周刊》的总编辑，杨大明和苗树彬为副总编辑，王烁为主编。其中，苗树彬为中国(海南)改革发展研究院的副院长，兼任《新世纪周刊》副总编辑。<br />
<br />
除此之外，胡舒立也被中改院聘任为高级研究员，杨大明和王烁被聘任为研究员和兼职研究员。根据中改院的官方网站所披露的内容，对于胡舒立等人的聘任决定是在12月29日作出的。<br />
<br />
《新世纪周刊》是中国改革开放后最早的一批新闻杂志，定位为综合类新闻周刊，1988年10月由国家新闻出版署批准成立。主管兼主办方是中国著名的智库机构―――中国(海南)改革发展研究院。<br />
<br />
创刊之初，这本杂志以时事报道为主，第一个报道了诸如“中国国内首例艾滋病”等在国内极具社会影响力的新闻事件，引起巨大轰动，单期发行量一度突破20万份。<br />
<br />
今年11月26日，该刊刚刚举办了创刊20周年庆典，同时还举办了首届“新世纪论坛”。有消息称，胡舒立入主之后，不仅将承担《新世纪周刊》所有遗留债务，还将留用前《新世纪周刊》全体员工。<br />
<br />
中改院的公告最后一句话有着这样的表述：自此，以胡舒立为带头人的新的编辑团队将着力打造全新的《新世纪周刊》。<br />
<br />
拖不起《财经》人马就此借壳<br />
<br />
来自胡舒立团队内部人士的说法称，2010年1月4日那一期《新世纪周刊》即原《财经》人马制作的第一期新刊物。“我们最近几个月都没闲着，已制作了几期试刊，那一期的内容就从试刊内容中选取。”<br />
<br />
该人士继续表示，原《财经》人马将就此实现“借壳”。“以后的《新世纪周刊》都是我们来做，1月中旬我们会推出第二期。”<br />
<br />
昨日记者连线了财新传媒新闻发言人，但其并未对此说法做出正面回应，仅表示截止目前没有再进一步的消息透露出来。另一位胡舒立团队的骨干则对此未予否认，且表示“我觉得应该没什么问题。”<br />
<br />
<br />
自11月9日胡舒立正式从《财经》辞职后，前《财经》近200人的采编和经营团队也随之出走并筹备创办包括网站和杂志在内的全媒体平台。12月18日，胡舒立正式出任中山大学传播学院院长一职，随后胡舒立团队即在中大官网下组建了“我们网”网站，并派出报道团队赴哥本哈根气候大会采访。<br />
<br />
不过此后有消息人士透露其原计划创办的新杂志《财经新闻周刊》刊号迟迟未获有关部门批准，不得已之下前段时间一直全力在做网站平台。此次借《新世纪周刊》的“壳”创办杂志完全出乎业界意料，但对“拖不起”的胡舒立团队无疑是重大利好。本报记者　刘昊<br />
