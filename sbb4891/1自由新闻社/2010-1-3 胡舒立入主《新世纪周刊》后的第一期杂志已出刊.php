													<h2>转帖：胡舒立的中国式接管</h2>
						
						
																											<div id="postmessage_416251" class="t_msgfont">若干年前，无论你有多么丰富的想象力，你都不会相信，胡舒立会介入一场非常典型的中国式接管。<br />
<br />
今天有人在这个帖子后面留言，怀疑胡舒立办不好《新世纪周刊》。我忍不住发表了自己的看法，她显然干得了，尽管，不见得能比老《财经》更辉煌。<br />
<br />
为什么这么说呢？主要是因为我了解她们那个团队的新闻运作能力，如果有必要，她们当然能够掘地三尺找到真相。但是，真正让我担忧的是，中国(海南)改革发展研究院的抗击打能力，是否就一定比联办强？是否就更有利于真相印在纸上？<br />
<br />
无论如何，胡舒立和她的团队总算有了个地盘，我衷心地替她们感到高兴。愿她们继续保持高水准的报道，为我们提供源源不断的好文章。同时，也希望新老《财经》人之间发生激烈的竞争，为中国的财经杂志奠定良好的发展基石。<br />
<br />
 <br />
<br />
说完希望，我想就事论事地谈谈胡舒立接管《新世纪周刊》这件事。有朋友认为这次接管很符合市场化的原则，而且契约至上。<br />
<br />
实话实说，我不太同意这种看法，我觉得这种新老交替的方式，从道义上说，有很大的问题，不符合胡舒立们一贯遵循的市场化解决方案。从某种意义上讲，这，不能说不是一种讽刺，一种典型的中国式接管……<br />
<br />
为什么这么说呢，根据公开的信息链条（假如这些信息大部分真实的话），我们不难发现，《新世纪周刊》采编队伍的解约太过匆忙了。根据劳动合同法，至少要提前一个月告知解约决定与理由，而且要给出选择方案。在双方未走完必要程序、未谈妥解约条件的情况下，原来的采编人员依然是《新世纪周刊》的员工，因此，是不能把他们的名字从编辑队伍里擅自取掉的。而胡舒立们出版的《新世纪周刊》，并没有印上这些人的名字。<br />
<br />
发生这样的事，实在是太令人惊叹了，完全不符合契约精神，也缺乏程序正当性。<br />
<br />
尽管，我对胡舒立的团队也很敬佩，但一码归一码，是与非，不能模糊。<br />
<br />
 <br />
<br />
这位朋友说，“之前的编辑团队被解聘，应该找资方而不是找胡舒立。”<br />
<br />
我觉得话虽这么说，但也不能免除胡舒立的责任。<br />
<br />
为什么呢？因为这就像一家工厂，新聘了一批经理和工人，在资方跟旧团队没有就解约问题达成合法有效协议的情况下，新团队就驻扎进去，把人家的工作岗位给实实在在地占领了。<br />
<br />
这个，符合契约精神吗？别忘了，原来的经理和工人是签订的合同的截止日期不是到现在为止，而是更远的未来。<br />
<br />
你根本就没有达成补偿协议，就强行把人家给下岗了，这就是典型的违约行为。因此，这次解约从法律上来讲是无效的，那些人依然是《新世纪周刊》的员工。<br />
<br />
这就带来一个严肃的问题，在一个协议尚未合法解除之前，你怎么能随便进驻呢？<br />
<br />
老《财经》的精神是什么？是法制精神、是市场规则，离开了这个，你以后还怎么批评别人呢？<br />
<br />
再说了，你急什么呢？你协助资方把问题妥善解决了以后，再进驻难道不行吗？<br />
<br />
为什么你急，因为你现在有了利益，你早进驻一天，你就可以早开工，早赚钱。要不然你没有平台，就得白养着一批人，那就是成本。<br />
<br />
可是，当年你批评别人为了利益违背规则，现在自己却在做同样的事。<br />
<br />
结论是什么，在利益面前，人人都缺乏免疫力，包括，曾经的批评者。<br />
<br />
 <br />
<br />
&nbsp; &nbsp; 这位朋友认为我在搞道德审判。我仔细想了想，觉得自己说的核心问题不是道德，而恰恰是法律与契约。因为，在旧的合同尚未合法有效地解除以前，人家原来那个团队仍然有主编、副主编、编辑、记者，美编，你哪里有权利把人家从杂志的出版人员名单里给删除掉呢？<br />
<br />
在某种意义上而言，中国之所以进步不了，就是因为我们一旦喜欢一个人，比如说胡舒立，我们就不肯承认她干的事情没有错的可能。<br />
<br />
胡舒立在这件事情上可以像驼鸟一样把头扎在沙子里吗？<br />
<br />
 <br />
<br />
有朋友可能要问了，为什么我要在这种司空见惯的事情上纠缠不清呢？我想，说到底，是因为太过敬佩胡舒立这个团队的采编能力，所以反倒为她们处理这件事情的方式之简单感到遗憾。<br />
<br />
尽管，在中国，这样的事情天天发生。可发生在胡舒立们身上，总感觉怪怪的。<br />
<br />
 <br />
<br />
最后，我不得不就胡舒立的这次中国式接管作出一点合理的猜测，而这种猜测，也完全是基于利益的。<br />
<br />
媒体的朋友大都知道，在新闻出版界，有个不成文的传统，一本杂志的经营与发行，最好是从一年之初开始，而不是从二月或者三月开始。<br />
<br />
不管是为了避免政治上的麻烦，还是追求经营上的完整性，胡舒立们选择了这样一种看起来天衣无缝的接管方式，一种似乎有人承担责任的接管方式。<br />
<br />
&nbsp; &nbsp; 但是，无论如何，这件事对胡舒立的团队，不能不说构成了一种伤害，尤其是，当我们以法制精神、市场理念来定义这支团队的时候。<br />
<br />
 <br />
<br />
&nbsp; &nbsp; 我的问题是：为什么连胡舒立，都不得不采取这样的下下策呢？</div>

							
							
							
							
