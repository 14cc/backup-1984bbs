<br />
网易传媒综合报道：胡舒立的动向总令人意外，就在业界认为原《财经》人马还在等待刊号的时候，胡舒立却“回马一枪”杀到了海南。昨日，来自财新传媒的确切消息证实，中国(海南)改革发展研究院(简称中改院)为了更好地推动《新世纪周刊》的发展，已聘任胡舒立等几位原《财经》高管担任《新世纪周刊》高管。<br />
<br />
实际上，胡舒立入主的背后是出走的原《财经》人马对于《新世纪周刊》的接手。据该团队内部人士透露，2010年1月4日的《新世纪周刊》即为原《财经》人马制作的第一期杂志，内容取自该团队最近一段时间以来所制作的几期试刊。<br />
<br />
今日上午，网易传媒已从内部人士处获得即将在1月4日出版的《新世纪周刊》杂志。封面设计风格与老《财经》极为相似，版权页上第一个即为“总编辑 胡舒立”名字，杨大明和原《新世纪周刊》总编辑苗树彬为副总编辑，王烁任主编，经营部总经理仍为吴传晖。其团队也几乎都是原《财经》杂志的人马，但也出现不少新面孔。<br />
