&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;广西南宁市横县警方在依法暂扣无牌无证摩托车时遭数十人围攻，致多名警员受伤。来自横县县委、县政府的信息透露，事发后，别有用心者在网络发布“交警在执法过程中有多名群众被殴打致死、致重伤”等虚假信息，影响恶劣。十二月八日下午，横县县委、县政府联合举行新闻发布会通报称，目前已有十四人涉嫌阻挠民警执法被依法行政拘留，另外十一人因未满十八岁或违法情节轻微，未执行拘留。<br />
<br />
　　通报称，针对横县乡镇摩托车入户率低、交通事故频发的情况，十二月一日，横县公安局交通管理大队组织二十六名民警和四十二名协警员到该县石塘镇灵竹街路段开展道路交通秩序整治工作。至当天下午三点二十分，交警共依法暂扣了二十四辆无牌无证摩托车，并开始将暂扣的车辆开往灵竹派出所。整个执法过程中，民警均按要求依法文明执法、文明执勤，无违法违规的情形发生。<br />
<br />
　　十分钟后，一些不明身份人员将执法小组围住，要求民警放行所有摩托车。在警力逐步撤离时，有人员躲在人群后不断起哄，煽动闹事，并三次向警方投掷石块、砖头、矿泉水瓶和垃圾桶等。在多次受攻击下，民警对三名仍不断向执法队伍投掷石块的闹事者依法采取强制措施，带到灵竹派出所问话。<br />
<br />
　　此后状况一度得到缓解，但在少数闹事者的煽动和怂恿下，一些不明真相者仍然围堵准备撤离的执法队伍，并搬来石块堵塞交通，阻止警车通行。警方随后开始进一步劝解，但遭少数闹事者冲过警戒线推拉执勤民警，一些在外围的闹事人员又再度投掷石头砸向警车和民警。经努力，警方最后制服了数名仍向执勤民警投掷石块砖头的闹事人员，并基本疏散了围观的群众，事态得到平息。至当日下午六点三十分，警力全部撤出。<br />
<br />
　　经初步统计，该事件共有三名民警被石块击中导致皮下瘀伤，一辆警车被砸受损，另有两辆过路民用车辆挡风玻璃被砸烂。<br />
<br />
　　警方表示，下一步将继续深化开展和改进全县交通秩序整治、预防特大道路交通事故的工作。</div>

							
							
							
							
