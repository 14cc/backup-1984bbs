													<h2>罗永浩：我将若无其事地归来开放</h2>
						
						
																											<div id="postmessage_19473" class="t_msgfont">我将若无其事地归来开放<br />
<br />
原文地址：<a href="http://www.luoyonghao.net/blogs/luoyonghao/archives/122435.aspx" target="_blank">http://www.luoyonghao.net/blogs/luoyonghao/archives/122435.aspx</a><br />
<br />
罗永浩 <br />
<br />
2009年1月9日下午3时许，收到一封来自万网客服的邮件，内容如下：您好！北京市通管局下发北京市政府新闻办公室通知：<a href="http://www.bullog.cn" target="_blank">www.bullog.cn</a>站点大量登载时政类有害信息，已要求该网站整改，但该网站仍未采取有效整改措施。现需要万网记录停止该域名的访问权限；<br />
<br />
HOLD域名bullog.cn；<br />
<br />
（原信如此，到上面的那个分号就没下文了）<br />
<br />
收到这封信后，过了一会儿，又接到我们的服务器托管商的客服电话，说机房接到通信管理局的通知要求关闭牛博服务器，我抢在最后的时刻用 Paparazzi!这个软件做了截图，留了个纪念。可惜牛博的版式在safari浏览器（Paparazzi!好像只支持safari）下显示不太好 看，footer甚至显示出错。<br />
<br />
（照片太大，影响速度，刚才暂时取消了。）<br />
<br />
由于无聊，我和黄斌在牛博网被关闭后，就我们俩谁表现得更平静进行了幼稚的攀比，结果是我觉得我们俩一样平静，他认为他表现得更平静。从这个结果来看，我在做人方面，生平第一次胜了黄总一个回合^_^。建议黄总看得淡一点儿。<br />
<br />
牛博关掉只不过半个多小时，就接到了美帝国主义的反动媒体时代周刊的记者电话，在此向时代周刊的高效率致敬。按照我一贯不接受外国反动媒体采访的做法，我对该反动记者表示了拒绝回答任何问题，除了确认被关闭的事实。<br />
<br />
又过了一会儿，一个同事对着电脑惊呼“我操！”，我们围上去一看，发现维基百科上的“牛博网”词条里多了一句“2009年1月9日下午，牛博网国内 服务器被关闭，牛博国际目前也无法登陆…”我们集体惊呼“我操！太牛逼了！这速度…”在此向伟大的维基百科的高效率致敬。<br />
<br />
接下来又来了几个外国反动媒体的电话，我照样拒绝了采访。美联社的记者助理极差的业务素质（“请问你们是牛什么网来着？”）和英国卫报的体贴（“我们可以对外讲是您本人承认了牛博被关闭的事实吗？”）都给我留下了深刻的印象。<br />
<br />
晚上回家发现<a href="http://www.bullog.cn" target="_blank">www.bullog.cn</a>这个域名已经被绑架到了一个“低俗”网站上<br />
<br />
<br />
内容的（在同样流量规模的网站里，牛博好像是唯一一家没有“低俗内容”的中国网站 了）牛博网的域名公然绑架到一个充斥着“胸模自述丰满秘籍”，“锁住男人的眼球，打造勾人魂魄的迷人乳沟”，“女性“紧窄”-老公最爱”，“做爱时阴道咣 当咣当的，宽得就像大操场－－阴松；做爱时阴道猛“放屁”，性致头上浇冷水－－阴吹；内阴外阴比虱子抓还痒，性生活后腰腹疼－－阴毒；阴道干涉得像河床干 裂，性爱变成撕裂痛－－阴干”之类的内容的网站上，是对党和政府的悍然挑衅。我在此嫣然建议有关部门从严从重追究此事。<br />
<br />
早晨醒来发现一口气睡了八个多小时，很久没连续这么睡过了，本来想给黄斌打个幼稚电话吹牛逼说我在牛博被关闭的当天夜里美美地睡足了八个小时，又怕 这小子接电话的时候假装还没睡醒就比我更牛逼了，只好算了。拿起设成了静音的手机一看，有一大堆陌生人和老朋友的安慰短信，陌生人的一律回了个“谢谢”， 老朋友的一律回了个“行了行了！都第四次了还这么大惊小怪的，该干嘛干嘛去。”<br />
<br />
高高兴兴地牛逼完了起床后，我又陷入了深深的忧虑和自责：没了牛博，从今天开始，人民看什么？！你要是不重开牛博，人民会答应吗？！你倒是睡得舒坦了，你有没有想过睡不着的人民怎么办？！怎么办？！！怎么办？！！！<br />
<br />
我在“怎么办”的回声中，带着浓浓的社会责任感毅然打开电脑，到百度和google的博客搜索里看了看网友的言论，发现关心牛博的朋友们的帖子里充 斥着“哭泣”、“悲伤”、“悲愤”、“想哭”、“郁闷”、“痛心”、“祭牛博”、“黯然”、“泣告”、“伤感”、“so sad”之类的字眼。<br />
<br />
唉，这帮没出息的东西，只好把我过去压箱底的必杀技热血青春励志故事再搬出来讲一次了（这是我听我年轻时期的偶像李敖老师讲的）。<br />
<br />
刘秀带兵打解放战争的时候，有一次吃了大败仗。夜里刘秀巡视营房，发现东汉解放军的军官和士兵们都很消沉，有唉声叹气的，有哭的， 有用阿杜的唱腔唱“擦干眼泪陪你睡的”，还有练毛笔字的（当然写的都是什么“郁闷”、“痛心”、“祭牛博”、“黯然”、“泣告”之类的破字儿），只有一个 人，坐在营房里不动声色的擦各种武器（七种？），准备第二天的战斗。刘秀说，这小子牛逼，能成事儿。李敖老师教导我们说，面对挫折的时候，不要哭天抹泪， 不要咒骂抱怨，不要悲观消极，要像这个人一样，埋头擦亮你的枪，准备下一次的战斗。<br />
<br />
――――――另起一行（一定要另起一行哦，效果不一样哦）――――――<br />
<br />
这个人就是大将军吴汉。<br />
<br />
故事讲完了。<br />
<br />
接下来说说昨晚的故事吧，要是昨晚有人刘秀附体到我家和黄斌家巡视，会发现我在家呼呼大睡，大摆混不吝的pose兼养精蓄锐，黄斌呢？正常熬夜（黄总很少不熬夜）做下一轮的牛博技术升级，抽空还随手抓了几个系统的小bug。<br />
<br />
――――――――――――――――――――――<br />
<br />
最后严肃地说点严肃的：<br />
<br />
牛博肯定会重开，这个不用担心，实在不行我就开一个海外的牛博国际，国内换个名字再开一个，可以叫驴博网。这已经说过很多次了。<br />
<br />
在牛博开博客的用户们请放心，牛博上的文章我们都有备份，所以不用担心你在牛博的文章会丢失，等到牛博重开的时候自己认领就可以了。这也说过很多次了。<br />
<br />
我个人不会有事，我只不过是办了一个让删帖就删帖，让删评论就删评论，让删整个博客就删整个博客的网站而已，不可能出什么事，大家不用自己吓唬自己了。<br />
<br />
感谢德赛公园的来信，感谢所有关心牛博的朋友。<br />
