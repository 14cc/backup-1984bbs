													<h2>山西不顾阻拦强行修建水库 截断河北40万人水源</h2>
						
						
																											<div id="postmessage_404497" class="t_msgfont">来源：长城网 2009年12月30日<br />
长城网邯郸12月29日讯（通讯员 李淑英 记者 郑建军）寒冬的太行山区河北涉县，满目萧索，裸露的山体如往年一样静待着人们冬闲时节的装扮。可是本该在田野忙碌的人们却因为山西下交漳水库的修建，斩断了他们赖以生存的水源，都蜂涌来到县委、县政府，市委、市政府讨说法，并准备不断地扩充规模向省部委、国家“进军”。<br />
<br />
　　“短短的2个月，已经有4批200多人来反映情况。如果国家和省部委不采取果断措施，以后恐怕会愈演愈烈，到时就是县里发动一切力量也难以制止。”涉县信访工作人员一边耐心地做着群众工作，一边拿出漳河沿岸16万群众的联名书说，本来平衡的用水环境，就因为山西打着修建泽城西安水电站的旗号，不顾中央部委的规划、阻拦，违法强行修建下交漳水库拦截清漳河全部水源，直接斩断了下游涉县40万人的“命脉”，间接影响到邯郸、冀中南地区的发展和群众的生产生活。<br />
<br />
　　那么山西是如何违法兴修水利？下交漳水库的修建会对下游造成什么影响？面对水利部海委的阻拦，山西又是怎么做的呢？带着这些问题，近日记者进行了走访调查。<br />
<br />
　　【质疑】<br />
<br />
　　搁置60年的修水库设想今朝假借建水电站之名实现<br />
<br />
　　国家《水法》第二十条规定：“开发、利用水资源，应当坚持兴利与除害相结合，兼顾上下游、左右岸和有关地区之间的利益，充分发挥水资源的综合效益，并服从防洪的总体安排。<br />
<br />
　　清漳河发源于山西，流经河北、河南等地，是一条跨省河流，清漳河水源是上下游共享资源。早年因抢水问题，三省之间就小有摩擦。在上世纪50年代，钱正英任水利部部长之时，三省为了各自的发展都规划了水库，当时山西省规划的就是下交漳水库，河北省规划的是刘家庄水库和匡门水库。为了论证水库的可行性，钱部长亲自带人沿漳河进行了考察，发现清漳河上游开发力度大，径流小，下游常出现断流现象，最后得出结论：如果在沿线再建水库，简直就是胡闹，因此各省修建水库的设想只好不了了之。<br />
<br />
　　水库设想破灭后，山西省并没有放弃利用地处上游之便，对水的开发利用，转而修建各类蓄水工程，拦截了绝大部分径流，磁县观台站实测的水文资料就是最好的佐证。据资料显示：1965年以前20多年清漳河的年均径流量为19.6亿立方米，1981―2000年20年平均径流量为3.56亿立方米，造成这一巨大的差距，降雨量年均减少5%固然是一个重要的因素，但实测径流减少80%就充分反映了上游用水量巨增的事实。如今，山西境内对清漳河的开发利用已高达81.4%，远远超过了国际40%的标准径流极限值。<br />
<br />
　　尽管大幅度开发利用了清漳河水资源，但山西方面仍未放弃修水库的设想，上世纪70年代，他们又规划设计了泽城西安水电站，1986年列入了国务院《海河流域综合规划》。按照规划，他们应该建设水电站，并且在建设前必须报经水利部海委审查并签署水工程建设规划同意书，对清漳河下游河北省等有关地区的影响作出定量分析，同时根据《水法》第十九条的规定，应当征求河北省水利部门的意见。但是由于他们知道自己是假借建泽城西安水电站之名建设下交漳水库，既违反了国家水法，又不符合国家规划，所以只好对这一投资高达10多亿元的大项目偷偷摸摸开工。<br />
<br />
　　对于泽城西安水电站和下交漳水库是一个项目两个牌子的说法，左权县委书记孙光堂在2009年7月28日在全县上半年经济分析会上的讲话最好的证明，他说：“……西安水电站（二期）工程，也就是我们过去所说的交漳水库，现在已经开工建设。”另外，泽城西安水电站建设的位置和规模、建设方式也都是有力的现实佐证。<br />
<br />
　　众所周知：建电站和修水库虽然同为水利工程，但在对水的开发利用上是不同的。电站是径流引水式的，是暂时把水蓄起来，发电后继续放到下游，并不影响下游的生产生活。而水库却是把径流全部拦截住，涝时放，旱时存，严重影响着甚至扼杀了下游的生存发展。<br />
<br />
　　【影响】<br />
<br />
　　为了少数地区的发展之需斩断大面积群众的“生命之源”<br />
<br />
　　水是一切生命之源。而清漳河贯穿涉县全境，40万人民的生产生活用水全靠该河补给。可以说，清漳河是涉县的“命根子”，是老区人民的“母亲河”，而且清漳河还要入岳城水库。如今山西为了个别县的利益修建下交漳水库，说是与下游互惠互利，实际上是意欲切断上游径流，不顾下游地区的发展和群众的生存，仅满足自己所用，将会对下游地区和群众带来灭顶之灾。<br />
<br />
　　涉县由于紧临山西，就成了直接和最大的受害者。首先对群众的饮水影响。涉县十年九旱，吃水贵如油，308个行政村、430个自然村的40万人的生活饮水全部依赖清漳河地表径流或补给地下水。由于近年来上游的污染，使得涉县成为了全国有名的癌症高发区。如果再截断清漳河基流，全县饮水问题不仅面临不安全的问题，更将面临无水可吃的严重问题。<br />
<br />
　　其次对农业生产和水利发展的影响。抗日战争时期，为了促进农业生产，刘伯承、邓小平等将帅率领一二九师官兵，倒提漳河水，修建了漳南大渠，又称为“将军渠”，修建了中国共产党历史上的第一座水电站。解放后，涉县人民用同样的办法又相继修建了漳北、漳西、青塔等4条万亩灌区和4条千亩灌区，修建了30多座水电站，成为全县农业和水利发展的“命脉”，如果下交漳水库落成，那么这些宝贵的水利工程就会报废，涉县将无水浇地可种，涉县是国家批准的以电代燃料重点项目示范县的称号将成为空壳，近亿元建电站投资成为泡影，2000多名水电职工将面临下岗。<br />
<br />
　　再次就是对工业发展的影响。涉县是工业大县，境内有全国500强企业的天铁集团、崇利制钢公司和投资近百亿元的国电龙山电厂，每年用水量达5000立方米，其中天铁集团是三线厂矿，曾为国家的发展做出了巨大的贡献，更是涉县的经济支柱。如果不能保证企业生产用水，将会给金融危机之下的经济发展雪上加霜。<br />
<br />
　　直接的损害固然触目惊心，但间接的损失更是不可估量。清漳河除了是涉县的生命之源，还是邯郸市800多万人生活的主要水源地，是工农业生产发展的重要资源。河流出涉县后，还将流经邯郸其他县区，供沿途42座水电站发电，最后入岳城水库。岳城水库是水利部直接管理的大型水库，担负着为邯郸市和安阳市的城市供水和农业灌溉任务。从近年来的水文资料显示，岳城水库从未蓄满过，如果下交漳水库再拦腰截断，该水库则将无水可蓄。<br />
<br />
　　【现状】<br />
<br />
　　不顾国家部委的阻止，山西强力推进建设进度<br />
<br />
　　2009年11月21日，漳河上游管理局受海委委托，对下交漳水库建设现场进行查看，并制作了影像资料。<br />
<br />
　　11月23日，河北省水利厅、邯郸市水利局到现场查看时现场已设卡，美其名曰为安全着想，实为阻止河北省人员近距离调查。<br />
<br />
　　12月3日，海河水利委员会政策处副处长刘红再次带领有关人员对现场进行勘察。<br />
<br />
　　12月15日，海河水利委员会向山西、河北水利厅发出紧急通知，要求山西泽城水电站暂缓施工，补办有关审批手续；要求河北做好沿河群众稳定工作。<br />
<br />
　　12月18日，海河水利委员会副主任兼漳河上游管理局局长王治、副局长杨士坤、水政处处长申志文一行五人到涉县，同漳河沿岸部分人大代表和水电站职工代表就下交漳水库问题进行座谈。<br />
<br />
　　12月27日，海委委派漳河上游管理局水政处到施工现场查看，由于公安把守，未能进入。<br />
<br />
　　……<br />
<br />
　　自从河北发现下交漳水库施工后，省、市、县高度重视，紧急通过组织部门，按程序及时向海河水利委员会、漳河上游管理局、国家水利部等上级部门逐级汇报，站在讲团结、求稳定的角度，请求上级部门进行协调解决。上级部门十分重视，紧急调查情况，拿出应对措施。<br />
<br />
　　12月15日，水利部海河水利委员会专门就山西省泽城西安水电站（二期）工程有关事宜下发紧急通知，要求山西省水利厅责成建设单位暂缓泽城西安水电站（二期）的施工，并按照规定程序向海委提出水工程建设规划同意书申请，提供可行性研究报告、与第三者利害关系的相关说明和与该工程建设没有利害关系的第三方编制的“泽城西安水电站（二期）工程对清漳河下游有关地区的影响评价报告。责成两省切实做好群众工作，避免事态激化，共同维护社会稳定。<br />
