													<h2>陈光标，首善还是首骗？</h2>
						
						
																											<div id="postmessage_851157" class="t_msgfont">晚上，一位朋友说到中国首善陈光标，我持谨慎的怀疑态度，愿意拭目以待。后来，看了朋友发来的陈光标公司江苏黄埔再生资源利用有限公司网站，粗略浏览之下，就发现问题。用我另一位朋友的话说，看了其公司网站，感觉陈光标是“他公司的主要产品就是“赈灾”，主要经营方式就是到处跟政府领导合影。”奇怪的是，这样一家连公司经营地址都没有的类皮包公司，竟然能够产生不下60个亿的利润，钱从何来呢？<br />
<br />
按照常识，一个企业首先要做好经营，创造价值，产生利润，然后才能够回报社会，当一个企业和企业家的大部分活动都跟经营无关，而又很有实力来做善事的时候，这个企业和这位企业家做善事的资本，钱从何来呢？<br />
<br />
按照陈光标自我描述，截止2010年4月，陈光标已经捐款累计12.3亿元；再根据陈光标要求裸捐的财产，当下有固定资产、房产和现金50多个亿。也就是说，从陈光标1990年南京中医学院毕业做针灸、推拿医生开始，1997年开始做生意，2003年开始黄埔再生资源利用有限公司，陈光标赚了60多亿，这是一种满打满算，只有收益，没有支出，而真实的情况，是绝非如此的。<br />
<br />
从陈光标做再生资源利用前的公司来看，电子医疗器械虽然能够赚钱，却绝不会产生超常的利润，陈光标的所有资产应该来自其后来的公司江苏黄埔再生资源利用有限公司。从其网站的公司简介里，我们看到如下信息：“多年来，公司先后参与了江苏、上海、北京、天津、广州、四川、香港、山东、山西等十多个省市的废旧拆除工程，拆除面积累计达2亿平方米，回收废旧钢材数百万吨，拆除下来的混凝土作为道渣使用，可供四车道的沪宁高速公路由南京铺设到上海，为当地的城市建设作出了巨大贡献。”我们先往高里假设2亿平方米拆除工程每平方米的报价是200元，这么多年来其销售额是400亿，我们再往高假设其利润为10%，那么，陈光标公司的利润应该在40亿左右，这其间就有了巨大的资金缺口。需要说明的是，我们以上所做数字都是只有进没有出，而企业实际运作则完全不可能如此计算。<br />
<br />
对于其公司运作方式和经营利润上，其公司网站有一篇报道这样描述：<br />
<br />
“江苏黄埔再生资源利用有限公司董事长陈光标把从事建筑垃圾再利用行业看成是在一个“看不见的金矿”里挖金，要真正获得金子是件“苦差事”。“靠拆除赚钱是最难的，往往是人家吃肉我啃骨头。”陈光标昨天向《第一财经日报》如是表示。<br />
<br />
江苏黄埔主要承担大型建筑项目的拆除工作，并从事建筑垃圾中的可再生资源回收、加工和再利用。他告诉记者，这个行业赚钱难点在于无法直接拿到拆除项目。 “企业业务中的95%都靠二手承包而来。”陈光标表示，在这个过程中能否拿到项目靠的多是权力关系。“拆除行业没有既定的行业操作标准以及法律法规，这让不少企业钻了不小的空子。”<br />
<br />
长安街扩宽项目拆除、央视过火大楼拆除工程等等……陈光标坦言，手中大项目虽然不少，但都是在为他人做嫁衣。他向记者透露，目前有不少能拿到一手项目的企业并不配备专业的拆除设备以及人员，而是将项目拆除权层层转包，由接手的二手、三手甚至四手、五手承包商来负责具体拆除。<br />
<br />
“拆除完成后，拿到一手项目的企业拿走废旧钢铁，留给二手、三手承包商的多为一些建筑垃圾。”陈光标坦言，一手企业的利润一般可以达到10%~13%左右，而二手、三手承包商的利润只能达到4%左右。<br />
<br />
于是，陈光标边拆除边总结，他告诉记者自己曾去德国考察该国对建筑垃圾的处理方式，他发现，德国可把建筑垃圾经过设备处理后变成陆地铺路的辅料，陈光标把这种垃圾处理设备称为“变形金刚”。<br />
<br />
大受启发的他回国后立刻从德国引进了6台“变形金刚”，每台每小时可产出陆基辅料200吨左右，产出的陆地辅料以20元/吨卖给第三方，在将建筑垃圾进行合理二次利用的同时，增加企业的盈利。<br />
<br />
目前，江苏黄埔在全国拥有大型设备600多台，包括挖掘机、推土车、吊车以及近80台从德国进口的“变形金刚”设备。2003年至今，江苏黄埔建筑垃圾产值397亿元，净利润26亿元。但陈光标坦言，由于每台进口机器售价高达500多万元，对企业的前期投入压力颇大。“如果按照传统将建筑垃圾填埋的方式运作，企业的利润一般可达到10%左右，所以现在业内几乎没企业愿意做垃圾二次利用处理。””<br />
<br />
从上面数据分析里，我们可以得出结论：陈光标的企业并不是富得流油，其赚钱的过程相当艰难非常辛苦，其企业的现金流也并不宽裕。要知道，现金流是一个企业的血液，而对于陈光标来说，现金流却从来不是问题，一个净利润26亿的企业，却能够从容用去十几个亿没有断血之虞，企业的运营成本、工商税收、应收账款，更像是玩儿似的，只能说明一个问题：要么，首善陈光标的那些捐助是注水的，需要在阳光之下曝晒，去掉水分；要么，陈光明另有见不得人的利润来源，这种见不得人的利润来源非常可疑。<br />
<br />
除了这些，我们还能怎样解释陈光标成就其首善地位的钱从何来的问题呢？<br />
