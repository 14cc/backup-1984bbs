　　昨晚国家发改委未宣布上调油价，中石化董事长涨价愿望落空<br />
<br />
　　■新快报记者 陈志龙<br />
<br />
　　中石化董事长上周放话称，成品油价最晚可于28日上调，近日这个消息一直牵动车主神经，昨天各网站也纷纷预测油价将上调。不过，昨晚国家发改委并未宣布上调油价，此次强烈的调价预期再次放空。记者昨晚采访发现，广州不少车主近几天都提前加满油，虽然此番未调价，但仍然神经紧绷，而且屡传“乌龙”消息令人很不爽。<br />
<br />
　　调价传闻再次放空<br />
<br />
　　5月22日，中石化董事长苏树林表示，目前按照60美元每桶的原油价格，中石化炼油业务就会处在一个亏损的状态，如果按照22个工作日计算的话，最晚到5月28日也可以调整价格了。<br />
<br />
　　多位石油专家表示，昨晚作为调价的又一个“时间窗口”具有合理性，当前调价也完全符合条件。根据新颁布的《石油价格管理办法》，当国际市场原油连续22个工作日移动平均价格变化超过4%时，即可相应调整国内成品油价格。<br />
<br />
　　据悉，随着4月27日上一个计价周期结束后，5月26日又迎来22个交易日计价期的结止日。据东方油气网首席经济学家钟健计算，在这个计价期结束的时候，三种国际原油(布伦特、迪拜、辛塔)平均价将达到每桶54美元左右，这一计价期的价格波动率仍远远超过了4%的可以调价的设定条件。<br />
<br />
　　自去年底以来，国内成品油价格已进行了3次调整。但近期国际油价持续上扬，而距离上一次国内成品油价格上调已有两月，这引起了市场对国家为实现经济“保增长、抑通胀”而对成品油价格从紧控制的猜测。<br />
<br />
　　昨晚有媒体向国家发改委求证，国家发改委对此表示：近期成品油价格不会上调。<br />
<br />
　　屡传涨价车主不爽<br />
<br />
　　记者昨天傍晚6时多走访广州市内多家油站，未见到车主排队加油的情况。天河区员村加油站一名工作人员说，昨天白天多了不少车来加油，不过，油价上涨的消息这几天已经让车主慢慢消化了，“最近几天已经多了些司机前来加油，有时甚至出现排小长队的现象，很多人都是冲着近两天就要涨价的传闻来的。”<br />
<br />
　　最近，油价频频疯传要涨，不少车主疲于提前扑油。在广州大道北一家油站，车主赵先生进到油站问：“今晚会不会涨啊？”当得到油站工作人员“应该不涨”的答复时，他不满地说：“怎么总搞这种‘飞机’呀？”他表示，虽然油价昨晚未上调，但调价预期很明显，因此神经仍然得不到放松，“今天不调，过几天可能就调了，所以我通常经过油站就会去加满油，能省点就省点啦。”<br />
<br />
　　不少车主对于近期屡次发生的涨价传闻摆“乌龙”很不满。车主林先生表示，老是说要涨价，搞到神经很紧张，“对于社会上的传言，发改委应该及时出来给大家一个明确的说法，不要老是搞突然袭击。”<br />
