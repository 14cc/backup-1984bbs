<strong>国家发改委辟谣：近期成品油价格不会上调</strong><a href="http://finance.qq.com/a/20090527/004924.htm" target="_blank">http://finance.qq.com/a/20090527/004924.htm</a><br />
<br />
<br />
中广网北京5月27日消息（记者冯雅）据中国之声《央广新闻》17时01分报道，中国之声记者刚刚从国家发改委有关部门了解到：近期成品油价格不会上调。 <br />
<br />
今天有多家媒体纷纷预测成品油零售价格可能在今天晚上上调。大家对成品油价上调的预期非常大，一是因为近日国际油价持续上涨；另外一方面中石化的老总估计最迟5月28号油价会有所上调。<br />
<br />
国家发改委有关部门今天明确表示，近期不会上调油价，也希望大家减弱对油价上调的预期，过一个快乐的端午节。<br />
<br />
<br />
--------------------------------<strong>我是谣言分割线</strong>-----------------------------------------------------------------------------------------------------------------------<br />
<br />
<br />
<strong>国家提高成品油价格 汽柴油6月1日起每吨涨400元</strong><a href="http://finance.qq.com/a/20090531/001244.htm" target="_blank">http://finance.qq.com/a/20090531/001244.htm</a><br />
<br />
<br />
近日，国家发展改革委发出通知，决定自6月1日零时起将汽、柴油价格每吨均提高400元。 <br />
<br />
自今年3月25日国内汽、柴油价格上调以来，WTI原油期货价格由每桶53.98美元上涨到66.31美元，国内成品油价格调整参考的国际市场原油价格由每桶45美元左右上涨至目前的57美元左右。根据完善后的成品油价格形成机制，决定上调汽、柴油价格，并综合考虑相关因素，适当缩小了价格调整的幅度。 <br />
<br />
通知要求，中石油、中石化两大公司要努力做好成品油生产和调运的衔接，确保市场供应。各级价格主管部门要加强价格监督检查，严厉打击各种价格违法行为，切实维护成品油市场的稳定。<br />
