<br />
据市场人士分析，此次调价涨幅温和，或也是基于减少国内经济复苏压力等原因。虽然未接近国外原油整体涨幅，但至少已经缩减了国内外价差，有助于保持国内炼厂加工积极性，保持汽柴油等油品的稳定供应。</div>

							
							
							
							
