																											<div id="postmessage_106554" class="t_msgfont">网易财经5月31日讯 国家发展改革委今日晚间发出通知，决定自6月1日零时起将汽、柴油价格每吨均提高400元。<br />
<br />
据多位业内人士表示，此次调价可谓是千呼万唤始出来，整个5月国内汽柴油市场都是在等待发改委最终落实调价，不过即使此番调价落实，400元/吨的调幅相对于原油的上涨亦被认为太过温和。<br />
