<br />
电脑评分 大学生恋爱将纳入诚信档案<br />
2009-07-15 Cbsi中国・PChome.net <br />
来源: 华龙网 责编: 杨剑锋<br />
<br />
 “言行当至诚，立世信为基。修合无人见，存心有天知……”这不是古代文人留下的传世名篇，而是即将向全国大学生推广的诚信守则。大学生的诚信度将通过网络软件，根据其日常行为来评判，并与其档案一起伴随一生。<br />
<br />
昨天（14日，下同），西南大学向记者证实，历时3年的教育部重点课题《当代大学生诚信工作实施和思想政治工作研究》，9月将全部结题。其中古诗体《大学生诚信守则》和全新的大学生诚信网络考核系统，都是国内首次亮相。<br />
<br />
古诗指引大学生诚信为人<br />
<br />
这份《大学生诚信守则》，共16句，前后押韵，朗朗上口。“是诚信守则，更是日常行为规范。”本次课题负责人之一的西南大学党委宣传部部长张国镛昨天表示，这份《守则》是征求意见稿，已在重庆市内几所知名高校中公布，只待斟酌修改细节，面向全国大学生推广。“融入古诗体例，是希望大学生们不要忘了传统文化。”但张国镛表示，“这份守则只是一个指南。我们不会要求学生背诵，也不会用守则条款来衡量一个学生的诚信度。”<br />
<br />
大学生诚信由电脑打分<br />
<br />
张国镛称，根据此次教育部重点课题的研究成果，今后大学生的诚信度将由一个专门的电脑软件来记录、评分，记入学生档案，“日常消费行为、思想政治学习、专业课程学习、求职应聘经历和人际交往等5方面情况，都将由辅导员或班主任详细录入电脑。”<br />
<br />
大学生在校恋爱“是否存在欺骗他人感情”的情况都将作为是否诚信的评判因素之一，而计入系统接受统一评分。<br />
<br />
“今后用人单位招聘，就可以根据其诚信记录和评分来权衡了。”他表示，目前该课题已进入收官阶段，9月就将全面结题上报教育部，等待面向全国高校推广。<br />
