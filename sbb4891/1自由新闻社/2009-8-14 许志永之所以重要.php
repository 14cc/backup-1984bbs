My Friend Zhiyong<br />
<br />
I met Zhiyong for the first time during the 2007 YLF Nanjing trip, although I have known him by name for quite some time. As most people know him, I was so impressed and inspired by his belief that China can be better. When most of the people stopped thinking about the future of China (as forbidden by the government), he still dreams about the future. We spent wonderful three days in Nanjing, and the longest talk happened in the bar near the Nanjing University. When most of the YLFer went to dance, Zhiyong, Nick Yu, and I sat around the table to discuss legal/moral/democratic processes of China for the whole night. I will talk about it later, but the short version is, I found I am inspired to run for a seat at Shanghai People’s Congress the next round, because in December 2003, running as an independent candidate, he won the only openly contested election for a seat in the Beijing People’s Congress. He said the law gives everyone the right to run for it, and why give it up?<br />
