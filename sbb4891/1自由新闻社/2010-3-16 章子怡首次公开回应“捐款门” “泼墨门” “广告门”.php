																											<div id="postmessage_518672" class="t_msgfont">章子怡：大多数的质疑是出于正义感(图)<br />
时间：2010/03 /16 出处：中国日报网<br />
<br />
<img src="http://img3.cache.netease.com/ent/2010/3/16/201003160052381f4b8.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
首次公开回应“捐款门” “泼墨门” “广告门”　―――<br />
<br />
　　经过两个多月的沉默，章子怡日前接受了《中国日报》独家专访，详细回答捐款风波中网友提出的各种质疑。章子怡否认有“诈捐”或其他违法行为，但承认自己从事慈善活动“缺乏经验”，并对长时间未能回应公众疑问作出解释。<br />
<br />
　　“捐给中国红十字会的100万人民币是我自掏腰包的”<br />
<br />
　　记者：捐给中国红十字会的100万人民币是你自己掏腰包的，对吗？<br />
<br />
　　章子怡：是。<br />
<br />
　　记者：当时实际只有84万到账。<br />
<br />
　　章子怡：对。<br />
<br />
　　记者：缺口的16万是最近才补齐的。<br />
<br />
　　章子怡：嗯。<br />
<br />
　　记者：这是为什么？<br />
<br />
　　章子怡：因为当时我没有在中国，我只是下达了一个（停顿）指令。我也没有再跟进这件事情，因为我在美国工作，然后又去了戛纳。我觉得我应该承担最主要责任。我没有直接去盯这个事情到底怎么样了。其实是工作人员中间有了数字上的误差……<br />
<br />
　　记者：你是什么时候知道捐的数字不是你想象中的数字？<br />
<br />
　　章子怡：直到后来有新闻出来，我们才具体去查。<br />
<br />
　　记者：那你有没有在任何时间、任何场合，以红十字会的名义，替你自己的基金会筹款？<br />
<br />
　　章子怡：没有。从来没有过。<br />
<br />
　　记者：你有没有想过把你捐的那100万人民币放在你的基金会里，通过你的基金会的形式捐出去？<br />
<br />
　　章子怡：没有。<br />
<br />
　　“戛纳募捐到的实际数字是1300多美元，如果当时的认捐人没有履行承诺，我会把那40万美元补齐”<br />
<br />
　　记者：戛纳的筹款活动受到了国内媒体的广泛关注。但是关于现场所得到的现金和承诺，有很多的说法。我们先来说现金。我看到最多的数字是5万美元，当然这中间包括了港币和欧元，折换成了美元的数字……这些数字正确吗？<br />
<br />
　　章子怡：不正确。<br />
<br />
　　记者：那真正的数字是多少？<br />
<br />
　　章子怡：真正的数字应该是，各种货币转换成美元大概是1300多美元的样子。<br />
<br />
　　记者：怎么会这么少呢？<br />
<br />
　　章子怡：坦白讲我们做这个事情也是非常仓促的，当时我们也没有场地，是（知名品牌）萧邦的一个好朋友 Caroline给我们赞助了一个小时的时间，用他们的场地……我身边的人非常有限……来的客人、来的朋友也非常有限，因为在戛纳那样的环境里，每一天大家都非常忙碌，兜里揣现金的人更是有限，尤其是老外，兜里可能……（无奈，笑）<br />
<br />
　　记者：当时现场承诺的捐款是多少？<br />
<br />
　　章子怡：承诺认捐的款项应该是40多万美元的样子。<br />
<br />
　　记者：那么到现在为止，这个承诺认捐的数字里面有多少已经兑现了呢？<br />
<br />
　　章子怡：兑现的认捐款项应该是3万9千美元（注：包括了现场拿认捐单、但事后汇去的1.5万美元）。<br />
<br />
　　记者：那么当时这个将近50万美元――包括现金和认捐加在一起的50万美元，是不是包括传（闻）中邓文迪女士要捐的那部分？<br />
<br />
　　章子怡：是的。<br />
<br />
　　记者：这两年来你有没有采取任何方式去提醒或者催这些认捐人来兑现他们的承诺呢？<br />
<br />
　　章子怡：有过打电话，也有过信函的往来。<br />
<br />
　　记者：具体的结果怎么样呢？<br />
<br />
　　章子怡：当我们有一个准确的项目去实施的时候，我希望拿着这个项目去给认捐人看，是不是他们也愿意并且有兴趣去做。<br />
<br />
　　记者：最终如果你不能筹齐这50万美元的话，你打算怎么办？<br />
<br />
　　章子怡：嗯（长时间停顿），如果真的认捐人没有履行他们的承诺，没有达到这个数字……我想就是给了我一个做慈善的机会，我会把这40万美元补齐。<br />
<br />
　　记者：你有没有在任何场合说过你已经募到了50万美元以上的数字，比如说，100万美元？因为这个数字出现是最多的了。除此之外还有200万美元、500万美元，我看到的最高的数字是700万美元。你知道是怎么来的吗？<br />
<br />
　　章子怡：（停顿）这些基本上都不是事实。我自己曾经说过一个数字，就是在筹款活动结束之后，我做访问的时候说，今天我们筹了将近50万美元。大家很兴奋，说如果可以再继续的话，你希望可以筹到多少钱？我说，我希望――我的目标是可以筹到100万美元。那关于200万美元的说法我也看到了，跟（男友）Vivi有关系的，说是他的朋友。关于这件事情我也求证过他，他也非常茫然。他说他完全不清楚，也没听说过有这样的事情。关于500万和700万美元，那是我完全没有听过的。<br />
<br />
　　“基金会的信息都是公开的，我们没有做过任何违法和不透明的事情”<br />
<br />
　　记者：有网友说，你实际上有两个基金会，一个是注册在加州的，还有一个注册在纽约。在纽约的这个地址他们查出来现在是一个英语学习班。这是怎么一回事？<br />
<br />
　　章子怡：我们只有一个基金会，确实有两个地址。我们的基金会是在美国加州成立的，在纽约是新加了一个地址，是为了联络通讯方便加的。<br />
<br />
　　记者：但是网友说，加州的那个基金会是以商业机构的名义注册的。<br />
<br />
　　章子怡：不是。我们的基金会是一个非营利的公共慈善机构。<br />
<br />
　　记者：有网友说，你这个基金会已经过期了。这属实吗？<br />
<br />
　　章子怡：基金会没有过期，而且是一直正常运作。<br />
<br />
　　记者：有网友查到，你这个基金会的账户里面只有45471美元。还有网友说这个账号里面的资产一直是零。这两个数字属实吗？<br />
<br />
　　章子怡：这个账号上基金会从来没有过零资产。4万多美元是怎么来的呢？应该是3万9的认捐款到账上，还有基金会需要的正常开支5000美元是我个人付的，加在一起应该是4 万多。<br />
<br />
　　记者：那是不是所有的捐款全部进入这个账号？<br />
<br />
　　章子怡：是的。所有的捐款从始至终都是在那里的。而且我想申明一点就是，这个基金会的信息都是公开的，我们没有做过任何违法和不透明的事情。大家都是可以查到的。<br />
<br />
　　记者：网上流传一个广告，是刊登在《好莱坞报道》这本美国影视界的行业刊物上的。在这个广告里，这个刊物的主编和你一起呼吁大家――应该是好莱坞的人士吧――一起为四川地震捐款。这个广告是不是真的？<br />
<br />
　　章子怡：是。<br />
<br />
　　记者：但是这个广告登了以后，你的基金会有没有人因为看到这个广告而捐款？<br />
<br />
　　章子怡：很遗憾，我们没有收到任何捐款。<br />
<br />
　　“‘德阳福利院’项目刚获批准，开工仪式后我们在三周之内把钱汇过去”<br />
<br />
　　记者：你之前说过，这笔钱是要用给一个叫做“关爱儿童”的组织，是要用来建造四川德阳的一个社会儿童福利院。但你基金会的这笔钱实际上是一直没有到“关爱儿童”的账号里。为什么？<br />
<br />
　　章子怡：因为“关爱儿童”组织在做现在这个项目的时候，必须要得到政府各个部门的批准。这个时间是很长的。<br />
<br />
　　记者：这个项目实际上政府是刚刚已经批准了。<br />
<br />
　　章子怡：对，是在2009年的11月份才批下来。<br />
<br />
　　记者：现在你这笔钱是打算什么时候汇到“关爱儿童”的账号上呢？<br />
<br />
　　章子怡：他们什么时候开工仪式开始，我们在三周之内把钱汇过去。<br />
<br />
　　记者：德阳这个福利院的总造价大概是多少？你和“关爱儿童”的捐款在里面大概会占多大的比例？<br />
<br />
　　章子怡：造价大概在900万人民币，我们会出450万人民币，大约是65.8万美元……40万美元是从我们的基金会（出的）。<br />
<br />
　　记者：这个项目结束以后，你会不会考虑关闭这个基金会，还是让它继续运存下去？<br />
<br />
　　章子怡：这个项目刚刚开始，刚刚批准，我们还是把热情和注意力都放在这个项目上。接下来会怎么样，我觉得日后的事情日后再说吧。<br />
<br />
　　“这次捐款的风波中，我发现有很多很多不一样的说法，最终我们想的是用法律的途径和方法来核实从头到尾的每个细节”<br />
<br />
　　记者：这一回捐款风波发生以后，你聘请了一个美国的律师事务所，来进行一个比较详尽的调查。这个事务所最近发布了一个声明，在这个声明里，非常明确地列出了你这次的捐款活动，包括里面的款项，做得非常详细……<br />
<br />
　　这份文件里涉及几个数额较大的捐款人，其中一个答应捐5万美元，另外（4人）各答应捐10万美元，他们的身份为什么没有公布？<br />
<br />
　　章子怡：是这样子的，捐款人有权利选择是公开自己的姓名还是不要公开自己的姓名，这里面涉及隐私保障。<br />
<br />
　　记者：如果他们最终没有兑现他们的承诺，实际上你是帮他们承担了他们的责任。<br />
<br />
　　章子怡：应该是这样讲吧。<br />
<br />
　　记者：关于这次捐款的风波，一共持续了大概两个多月。你为什么不尽早出来澄清呢？<br />
<br />
　　章子怡：我一开始就想出来讲的。但是这个过程中，我发现有很多很多不一样的说法，最终我们想的是用法律途径和方法来把这个核实――从头到尾的细节。我想这样子可能会更有说服力。所以在这过程当中，我们要把所有证据全部送到美国去，证据包括现场填写的认捐单，还有现场的现金，收到的现金，还有现场所有和这次募捐活动有关系的证据，我们都要拿到美国去。这个过程花了不少时间。<br />
<br />
　　“我觉得我一个人去承受这些是应该的，因为这是发生在我身上的”，但“我真的不希望再有人承受和我一样的麻烦和痛苦”。 “代言方面没有任何事情发生”<br />
<br />
　　记者：那你觉得这当中是有人在幕后操纵，还是网友自发的，希望了解真相而提出质疑。<br />
<br />
　　章子怡：我相信大部分网友是出于正义感，他们也会实事求是。当大家知道事情真相的时候，我相信每个人都有他们自己的判断力。<br />
<br />
　　记者：这两个月以来，这场风波对你和你的家人造成了什么样的影响？<br />
<br />
　　章子怡：影响很大。（停顿）我觉得我一个人去承受这些是应该的，因为这是发生在我身上的，但我最不想看到的是，家里人跟着我一起去担心（哽咽）……<br />
<br />
　　记者：有人说你退出《雪花密扇》这部影片是因为这件事情，而且一些你代言的品牌，包括美宝莲已经放弃了你，这个说法准确吗？<br />
<br />
　　章子怡：不准确。<br />
<br />
　　记者：真正的事实是什么？<br />
<br />
　　章子怡：《雪花密扇》这部戏我已经花了两年多的心血，一直在筹备。突然开拍之前我受伤了，因为在拍动作戏的过程当中，我动了一个手术。<br />
<br />
　　记者：那代言方面的事情呢？<br />
<br />
　　章子怡：没有任何事情发生。<br />
<br />
　　记者：你目前正在做的工作是什么？我指的是电影这方面。<br />
<br />
　　章子怡：我目前继续完成王家卫导演的《一代宗师》。接下来我会和顾长卫导演拍一部戏……这部戏跟（停顿）慈善有很直接的关系。<br />
<br />
　　记者：最初“泼墨门”的发生是跟捐款没有关系的，是因为那位赵姓女士说，她和你之间有一些事情。她和媒体说的话里有很多是对你的指控，你愿不愿意作出回应？<br />
<br />
　　章子怡：我想这件事已经给很多人造成了不必要的麻烦，尤其是我的家人，我自己也承受了很大的压力和痛苦。我真的不希望再有人承受和我一样的麻烦和痛苦。<br />
<br />
　　记者：你希望你的恋爱和婚姻被公众知道吗？<br />
<br />
　　章子怡：我觉得如果是堂堂正正的事情，你又是一个公众人物，是有必要让公众知道的。<br />
<br />
　　记者：你是中国少数具有国际知名度的明星，正因为此，有人说你的一言一行代表着国家，你应该受到国家特别的保护，或者说你应当受到特别严格的约束。对这（些）观点，你怎么看？<br />
<br />
　　章子怡：我希望自己做得尽量好一些，但我不希望给自己太大的包袱、太大的使命感。我还是想做一个普通人，但我会尽量努力，让自己做得更加完美。<br />
<br />
　　记者：你希望你的公众形象是怎样的？<br />
<br />
　　章子怡：我希望当一个有爱心、敢担当、有责任心、坚强勤奋、有个性的女孩子。<br />
<br />
　　■文/《中国日报》周黎明</div>

							
							
							
							
