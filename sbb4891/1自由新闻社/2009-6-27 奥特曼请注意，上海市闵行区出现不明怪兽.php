新华网上海6月27日电（记者刘丹 杨金志）27日5时30分许，上海市闵行区莲花南路罗阳路口，一在建楼盘工地发生楼体倒覆事故，造成一名工人死亡，无人受伤。<br />
<br />
　　事故发生后，上海市委、市政府主要领导高度重视，市长韩正要求有关部门迅速调查处理，确保群众生活不受影响。<br />
<br />
　　事故发生在淀浦河南岸的“莲花河畔景苑”，发生倒覆的是一栋13层在建住宅楼。由于此楼尚未竣工交付使用，所以未酿成居民伤亡事故。<br />
<br />
　　记者在现场看到，倒覆的住宅楼横“躺”于地，所幸周边数栋在建楼房未受损。据工地人员透露，工人们近日正在倒覆住宅楼附近进行地下车库施工。<br />
<br />
　　记者从有关部门了解到，26日邻近的淀浦河防汛墙出现了70余米塌方险情，有关方面连夜组织抢险工作。27日晨，河畔在建的这栋13层高楼突然整体倒覆。警方透露，在事故中丧身的工人为安徽籍民工，姓肖，28岁。事发时，他正在楼里取工具，楼房倒塌前他未来得及逃出不幸丧身。<br />
<br />
　　目前，有关部门正加强周边建筑物的安全检测，确保群众生活不受影响。事故原因目前正在调查之中。<br />
