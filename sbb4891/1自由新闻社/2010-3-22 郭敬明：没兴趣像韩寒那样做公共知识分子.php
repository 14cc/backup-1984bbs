													<h2>郭敬明：没兴趣像韩寒那样做公共知识分子</h2>
						
						
																											<div id="postmessage_526437" class="t_msgfont">他的身份频繁转换，有偶像作家、老板、主编、“副处级”领导；他的新闻多如牛毛，爱晒自己所用的奢侈品物件“炫富”、博客上大秀上海汤臣一品的豪宅、在2009年度中国作家富豪榜上曾两度夺魁的郭敬明以1700万屈居第二……昨日，出版天王郭敬明携旗下团队成员陌一飞、李枫、阿亮来汉宣传《最小说》，在崇文书城咖啡吧接受本埠记者群访不足12分钟，他在回答本报记者关于他和韩寒手牵手PS照片的问题时，脸上明显有一丝不快，嘴里嘟囔了几句话，没有大声说出来。想了片刻，他摇摇手、摆摆头发，勉强挤出几个字：这个算了吧。<br />
<br />
记：你现在是主编、商人、作家等，这么多工作，有时间专心写作吗？<br />
<br />
郭：因为我的写作习惯是晚上才会写东西，白天不太习惯写东西的状态，一般是夜深人静时写作，与我白天的工作不冲突。只是私人的时间变得很少，不太有时间玩呀，出去旅游呀，大部分时间都在工作，不写作的空余时间用来做自己的公司和杂志，时间安排上比以前更充实，两者之间的时间是区分开的。<br />
<br />
记：你创作的一系列作品《小时代》，都是大城市时尚生活，有否打算写童年时在家乡轻松自然生活？<br />
<br />
郭：我写了很多，前三本都是在写初中、高中、我的学生年代和小城市。<br />
<br />
记：你和韩寒是青春文学领军人物，随着年龄增长，会越来越成熟，你的书阅读对象主要还是中学生，你的写作上有些停滞？<br />
<br />
郭：我从来没有考虑我的作品是写给中学生看的，我是在写我自己的内心。我身边有很多成年人甚至比我年纪大的人去看，只是他们不会像年轻人这么狂热，比如说来现场参加签售会，这是他们很年轻激情的一面，属于他们会有的东西，成熟一点的读者看了之后，只是在家看完，不发表评论，不参加活动。随着我自己的年纪增加，当初看我书的读者也在慢慢长大，也有更年轻的读者加入我的读者群，我不会去设定、限制一个我写给谁看的范畴，至于社会上谁会喜欢，读者们自己去选择。<br />
<br />
记：在你心目中一部优秀文学作品须具备什么特点？<br />
<br />
郭：要有独特的辨识度，作者个人独特风格，不同于这个也不同于那个，只属于你的真实感情，不是看完书就完了，一定是触动了内心，有些共鸣和思考，感情的真实对我是最重要的。<br />
<br />
记：对风行的网络小说泛滥有何看法？对网络文学写作者有什么期望？<br />
<br />
郭：其实网络只是写作载体，大家真正看的还是小说，只是在电脑上看和看书的区别而已，但真正的故事还是故事，这个没有本质上的区别，好的小说无论是网上看还是在纸上看都是一样的。只是电脑和网络这种发达的渠道，让越来越多的写作者可以有更方便的途径和更低的门槛，每个人都写，这也造成了，一方面天才会更容易发现，另一方面造成参差不齐的作品，大量是粗糙的，少量是精品，给读者带来很多选择性困难，要看好多篇才能看到精品。<br />
<br />
谈写作：成年人也看我的作品<br />
<br />
被娱乐：无所谓吧，大家有谈资聊一聊<br />
<br />
记：首站选在武汉，这次活动有什么计划？<br />
<br />
郭：这次活动跟《最小说》上举办的文学之新大赛相关的，希望喜欢我的学生们，一来参加比赛，二来算是与年轻朋友们的见面会吧，我可以以过来人的身份与学生们交流一下写作经验与我个人的经历。接下来会走其他城市，4月会去成都。<br />
<br />
记：有个同学想问你的商业价值，你觉得你的身价有几个零？<br />
<br />
郭：我不太清楚，我也不知道有几个零，人希望自己越来越好呀，但目前还不知。<br />
<br />
记：你是90后的偶像，你觉得最能吸引他们的特质是什么？是特立独行吗？<br />
<br />
郭：我没想过这个问题，你问他们比较明白。我其实还不算特立独行，我算是比较大众的性格，比较主流的性格。<br />
<br />
记：你和韩寒是被娱乐化和消费化的人，你是高兴还是悲哀？<br />
<br />
郭：无所谓吧，都可以，大家有谈资聊一聊，对我来说，没有太大关系，我还是集中精力做好自己的事情，写好小说办好杂志，这也是读者希望看到的，至于聊些新闻呀、今天又发生了什么，这些很快会过去，这个不重要，不会影响我的生活。<br />
<br />
记：继你开办主编杂志后，越来越多的80后作家当主编、办杂志，这些后起之秀会否给你造成压力？<br />
<br />
郭：我在很多场合被问到这个问题，今天谁谁谁又做了杂志，不存在什么压力，因为世界上有几千上万种杂志，不会多了一本或少了一本，其他杂志就做不下去了，我觉得市场很大，大家各自做自己的杂志，只要是好作品好杂志，都会被别人喜欢，这是很正常的事情。<br />
<br />
记：网上流传有一张PS照片：你和韩寒手牵手，不明就里的人还以为你们在闹绯闻，你怎么看？<br />
<br />
郭：……这个问题算了吧。跟今天主题无关系。<br />
<br />
记：最近韩寒有个新闻，他主编的杂志缺钱，肯定要卖作品或卖身来为杂志筹钱，你又是一个成功商人，如果他来找你筹款，你会把他纳入团队中吗？<br />
<br />
郭：我不太会回答这些假设性的问题，假设的不回答。<br />
<br />
记：越来越多读者认为，韩寒有个转型，他在往年轻的公共知识分子这个方向转，你对这方面有兴趣吗？<br />
<br />
郭：没兴趣。一来性格不是这方面，二来我有自己很喜欢的事业，我喜欢做一个小说家，喜欢写书，喜欢做杂志，喜欢继续从事出版行业，这些年这一直在经营这方面，也作出了很多的成绩，我很热爱这个行业，也喜欢自己现在的工作，所以没有觉得有什么不好，需要我去转行的地方。<br />
<br />
昨日，郭敬明携旗下团队成员陌一飞、李枫、阿亮来到武汉，与江城读者进行了亲密接触。2010年《最小说》青春写作进校园大型活动在武汉二十三中启动。活动现场郭敬明及团队成员通过一系列有趣的互动形式，与在场的2000多名师生深入交流写作技巧。<br />
<br />
曾获得戛纳广告金狮奖的国内顶尖插画师陌一飞讲述了《小时代1.5青木时代》的创作历程。首届文学之新8强选手李枫在分享自身写作经历时，就坦承文学之新大赛给自己带来前所未有的成就感，没有文学大赛和强大的《最小说》平台，就没有处女作《燃烧的男孩》大卖，《青春白恼会》漫画的作者爱礼丝（阿亮）介绍了《最小说》杂志选稿标准，郭敬明的出场掀起了活动的第一个高潮，场下不时传出女粉丝尖叫声，见惯了大场面的郭敬明介绍了文学之新大赛的举办宗旨，讲述自己从写作迈向成功的两段经历，分享平日写作心得。面对着台下手拿大赛报名表的莘莘学子期盼的眼神，郭敬明真挚的表示“拥有更豪华评委阵容的第二届‘文学之新’大赛报名已经开始，将为每一个对文字充满敬意和热爱的人敞开大门，你将在TN大赛的平台展示自己的才华，甚至可以成为下一个郭敬明。”<br />
