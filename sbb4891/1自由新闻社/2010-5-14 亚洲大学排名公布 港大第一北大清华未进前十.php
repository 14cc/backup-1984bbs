根据该调查公布信息，中国大陆有5所大学入选前30，分别是北京大学、清华大学、复旦大学、中国科技大学和南京大学。其中以老牌名校北京大学排名最高，排12位；名校清华大学及复旦大学亦分排16位及24位。<br />
研究报告称，位列前200大的内地院校的研究成果突出，不少论文被引用，但由于香港院校始终较国际化，故排名仍超越内地院校。整体而言，香港院校的教员国际化上取得满分100分；至于学生国际化，则有4所港校取得100分。<br />
文章称，虽然对中国内地高等教育未来的投资正在逐步显示出成绩，但其在“师生比”中较低的得分则显示，中国内地大学还有许多地方值得努力，比如在吸引更多的国际人才和增加教学资源上。<br />
<br />
QS 亚洲大学排名是QS世界大学排名的延伸。QS世界大学排名始于2004年，是世界范围内被引用最多的大学排名。<br />
