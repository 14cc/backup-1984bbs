2010年05月13日11:03环球网<br />
<br />
环球网实习记者薛珊珊报道，据香港文汇报5月13日消息，根据英国高等教育调查公司QS公布的2010亚洲大学前200名最佳大学排行榜，香港大学蝉联排行榜第一，香港科技大学和新加坡国立大学紧随其后。<br />
