华西都市报3月22日报道 青年男子杨化军（化名）平时喜欢上网，一次偶然机会加入了一个QQ群，群里面的成员不时在群上上传一些淫秽、色情视频和图片。出于好奇，杨化军经常浏览这些黄色淫秽物品，久而久之，他开始自己疯狂地下载这些黄色东西，供自己随时“欣赏”。直到他被当地警方抓获，他还不知道自己的行为违法了。<br />
