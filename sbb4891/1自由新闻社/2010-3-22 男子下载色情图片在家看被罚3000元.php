<br />
宜宾市及南溪县两级网监部门的案侦民警由两人组成一组，对多达近30余条的线索进行逐一摸排、查证。经过近半个月的调查，一个熟悉的QQ号多次跳入警方的视线，经过查证，该QQ号经常从网上下载含有淫秽色情的视频和图片，它的“主人”杨化军（南溪县某事业单位工作人员）迅速被警方锁定。<br />
<br />
提醒：下载淫秽物品也违法<br />
<br />
<br />
据南溪县公安局网监大队大队长刘应南表示，在抓获嫌疑人杨化军时，他不知自己下载黄色淫秽物品触犯了相关法律，还以为自己在家看点带“色”的没有招惹谁。他提醒市民，要重视当前虚拟社会与现实社会相连通，网上与网下相互联动，人们在虚拟世界里若行为“不端”同样可能涉及违法犯罪。<br />
<br />
刘应南指出，鉴于嫌疑人杨化军在家观看淫秽物品，并未向外传播，且没有以此作为谋利的工具。根据1997年由公安部发布实施的《计算机信息网络国际联网安全保护管理办法》有关规定：任何单位和个人不得利用国际联网制作、复制、查阅和传播淫秽、色情信息，网监部门对嫌疑人杨化军处以警告及罚款3000元的处罚。<br />
<br />
<br />
后续(23号更新)<br />
南溪县公安局网监大队大队长刘应南根据的是1997年公安部《计算机信息网络国际联网安全保护管理办法》的有关规定。<br />
<br />
不过，根据2006年3月1日起施行的《中华人民共和国治安管理处罚法》规定，“制作、运输、复制、出售、出租淫秽的书刊、图片、影片、音像制品等淫秽物品，或者利用计算机信息网络、电话以及其他通讯工具传播淫秽信息的”才构成违法，不再规定“浏览”、“查阅”淫秽物品构成违法。《办法》是规章，《治安管理处罚法》是法律，并且《治安管理处罚法》更晚制定，根据“上位法优于下位法”、“后法优于前法”的原则，“浏览”、“查阅”淫秽物品理应不能再认定为违法。<br />
