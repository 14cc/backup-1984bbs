昨日，宜宾市南溪县公安局网监大队负责人透露：该大队刚侦破了一起由公安部交办的涉嫌利用互联网络传播、复制淫秽物品的案件，嫌疑人杨化军被警方抓获。<br />
<br />
意外：在家看黄图自娱被抓<br />
<br />
2月26日上午10时许，家住宜宾南溪县城区的杨化军刚关掉电脑，正准备从家中外出时，突然听到一阵敲门声，他以为是亲戚来家做客，急忙去开门。打开门一看，门外全是一张张陌生的面孔。这几位陌生人正是宜宾市公安局网监支队、南溪县公安局网监大队联合侦办组的案侦民警。<br />
<br />
民警依法对杨化军家的一台笔记本电脑和一台式电脑硬盘进行了检查，查出大量从网络下载复制的涉嫌淫秽色情物品，包括视频文件39个（约7G），淫秽黄色图片526张（约80M）。落网后，杨化军向警方交代，自己一直喜欢上网，大约半年前自己偶然加入了一个QQ群，群里面的几十个成员不时在群上上传一些淫秽、色情视频和图片，包括网上热炒的一些娱乐名人的情色视频和照片，如“艳照门”中一些女星的情色照片。<br />
