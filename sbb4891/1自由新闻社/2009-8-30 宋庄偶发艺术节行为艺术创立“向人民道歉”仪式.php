<br />
吴鸿<br />
<br />
<br />
在宋庄的早期艺术家群体中，有一个流传甚广的笑话：说在十几年前，有两个落魄的艺术家来宋庄准备租房子，四处碰壁之后，哥两个几近绝望，一合计，再往村东头看看吧。这时候，村里的高音喇叭响了：各位村民注意了！他们两个又往东头去了啊……<br />
<br />
 <br />
<br />
听过这个笑话之后，大家都会：哈哈哈……。这是一阵含着泪水的笑声。但是，再看看现时宋庄的模样，这个笑话已经几近黑色幽默了！<br />
<br />
 <br />
<br />
“宋庄”是什么？在行政区划的概念上，它在北京郊区县通州的一个下属乡镇。而在中国当代艺术的概念里，它的涵义则要更加丰富得多。<br />
<br />
 <br />
<br />
在中国当代艺术的概念中，它最初沿承着圆明园的一丝游脉，在当代艺术发展最严酷的阶段中，它是艺术家的精神庇护所，是身体的暂栖地；在文化时尚化来临的阶段，宋庄是时尚媒体们梦寐以求的波西米亚精神的典型，小记小编都忙不迭地希望能在这里发现一些异行状，以满足那些都市白领们的精神意淫；而在艺术市场化的初期，宋庄则又代表着一种无政府主义下的“原产地”属性，和“城乡结合部”概念下的混乱和无序，是掮客们介天带着洋鬼子和假洋鬼子出东入西的天堂。但是，在任何时候，宋庄都会有一帮自命不凡的“寒人们”指天骂地，牢骚满腹，有人说他们是宋庄的毒瘤。<br />
<br />
 <br />
<br />
而今天的宋庄已经越来越“798化”了！<br />
<br />
 <br />
<br />
今天的宋庄是一个财富比拼的场所，今天的宋庄是一个机会主义者淘宝的天堂，今天的宋庄是一个理想和原则可以用来兑换现金的交易所，今天的宋庄是一个梦想和青春连同土地一道都可以“开发”掉的乐园。<br />
<br />
 <br />
<br />
所幸的是，还有一帮“寒人们”，他们是残存的波西米亚风格的无政府主义者，他们是在宋庄越来越边缘化的群体，他们将像濒危物种一样在宋庄越来越罕见……<br />
<br />
 <br />
<br />
但是，他们是真正的代表着原教旨意义上的“宋庄精神”的直系血脉，因为还有他们，宋庄才看起来还有点像“宋庄”。如果他们在最后的栖息地被赶尽杀绝，宋庄的空气中将只剩下一股浓浓的铜臭屁……<br />
<br />
 <br />
<br />
