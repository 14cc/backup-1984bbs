																											<div id="postmessage_220601" class="t_msgfont">宋庄偶发艺术节行为艺术创立“向人民道歉”仪式<br />
<br />
<br />
<br />
&nbsp;&nbsp;2009年8月26日是北京宋庄艺术区第一届”现象偶发艺术节“召开的日子，这是一次纯民间自发操办的当代艺术节，地点选在风景秀丽、视野开阔的潮白河边，当天的各类艺术活动五花八门，唯以行为艺术最为惹眼，现场高潮不断。笔者受邀前往观看，恰好目击到一次难得一见而又耐人寻味的行为艺术活动，特此记录下来。<br />
&nbsp;&nbsp;因为是偶发艺术节，允许观者互动和参与，行为艺术板块的节目一场场下来，轮到一位精神矍铄的老艺术家周永阳在现场表演一个题为“向人民道歉”的行为艺术作品，只见周老先生身后几步远的现场草地上竖立着三面巨大的彩扇，分别由三条汉子撑扶，笔者仔细看了一下，扇子是用天蓝色尼龙绸子做的，周边装饰着各色彩带，三面彩扇两面都上都书写着一些大字，扇正面上写着“向人民道歉”，下书“罪己诏”三个大字，背面写着“周永阳思想研究所”。由于现场位于潮白河河岸边上，场地空旷，这三面大扇子犹如巨旗树立，远远的就分外惹眼。<br />
&nbsp;&nbsp;再看周永阳本人的打扮，更是奇特，但见满脸美髯长胡须的他赤脖着上身，其额头上缠着一条蓝色缎带，上书几个清晰大字也是“向人民道歉”。而他赤膊的后背上还用缎带斜捆着一捆荆棘和枯枝，下穿短裤脚蹬凉鞋，一脸的庄严肃穆神情，面朝着观众站好。<br />
&nbsp;&nbsp;果然这一惹眼另类的场景和他这身打扮立刻吸引了许多现场观众和参展艺术家前来围观，大家面对着老周屏住呼吸，静待他下面有何动作。<br />
&nbsp;&nbsp;行为开始，身背荆棘的周永阳先向面前的观众开口宣讲，他说：“我今天是想在此设立一个“向人民道歉”的仪式，希望能创立一种向人民道歉的模式，为什么要设立向人民道歉的仪式呢？因为现在有很多人做过对不起人民的事，小到偷窃过人民的财产，小偷小摸，大到对人民的庄严承诺没有履行，等等，总之是对不起人民，我这个仪式是专为那些本该向人民道歉，却还没有道歉的人，或者还想不起要向人民道歉的人，或者还不会给人民道歉的人做一个示范。”<br />
&nbsp;&nbsp;老周说的掷地有声、慷慨激昂，闻听此番讲解，观众中立刻略微骚动起来，有人在轻声叫好，低声窃窃私语中纷纷传出“这个行为太好了”云云。此时现场的另一位行为艺术家片山手持一柄巨斧走到老周身后想为其助阵。大家也更加好奇老周下一步的动作。<br />
&nbsp;&nbsp;接下来老周顿了顿，满脸肃穆的面朝观众双手下垂，提高嗓门，一字一顿的缓缓说道：“向――人――民――道――歉！向――人――民――道――歉！”口里一边说，身体一边缓缓弯腰，面向在场的全体观众深深鞠躬，就这样周永阳连续连说带鞠躬的重复了好几次“向人民道歉”，这时现场观众已是鸦雀无声静得出奇，仿佛要被这一场景凝固住了，这时老周又开始继续宣讲，只听他说：“过去人民一直站着，而我们一直坐着，今天，我向你们道歉来啦，对不起你们啦，向你们道歉，向―人―民―道―歉！”继而又是一系列的鞠躬。周永阳继续说道“前些天南方周末有一篇文章，叫做《好政 府全都是骂出来的》，好孩子不是打出来的，但是好孩子都是管教出来的，而好政府呢全都是骂出来的。不骂是不会变好的。”&nbsp;&nbsp;此时，现场的气氛开始热烈起来，人们纷纷给老周叫好，这时忽然有人提议：“咱们跟老周一起“向人民道歉”吧！”，大家兴奋的立刻用实际行动来响应号召，在场的艺术家和观众纷纷走向老周，大家簇拥着老周自动站成两排，只听老周一声“向――人――民――道――歉”响起，大家都纷纷面向人群集体低头深鞠躬，然后是同声的高呼“向人民道歉”，同时集体深鞠躬，一次、两次、三次。。。只见道歉者们对面的观众们一个个都在忙着拍照摄像，生怕不能保留住这难得一见的生动的场景。<br />
&nbsp;&nbsp;周永阳看到这观众自发的参与和偶发现象，也非常兴奋，他趁热打铁跟大家讲到：“我还有一个行为艺术叫做‘指天骂地’，我呼吁大家都能参与进来，我们也能感动上帝的，这个上帝不是别人，就是全中国的人民大众，全中国的人民都来和我们一起‘指天骂地’，中国的什么问题都可以解决了！” 于是，现场的观众们又纷纷举起手，一起用手指向苍天，做出”指天骂地“的动作，现场气氛再一次达到高潮。<br />
&nbsp;&nbsp;艺术家同行和在场观众纷纷上前与周永阳合影留念，诗人、艺术家俞心樵第一个跟老周握手致敬，说：“你的这个行为作品非常好！也给了我很大的启发。”看得出来，大家的心情都很激动，连不远处传来的一阵急促的汽车警笛声也没有打断现场热烈的气氛。<br />
人群在兴奋的欢笑和鼓掌声中结束了这次偶发现象的行为艺术活动，也许人们都愿意相信，能有这么一天，周永阳发起的这个“向人民道歉”的行为艺术，不仅仅是民间偶发的一次性的行为艺术现象，而能真真正正成为那些该向人民道歉者要公开表达歉意的庄严仪式。<br />
&nbsp;&nbsp;这次亲眼目击的“向人民道歉”的行为艺术活动不仅难得，而且耐人寻味，故特此记录之。<br />
