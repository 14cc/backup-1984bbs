													<h2>伍皓：我不是五毛 是党委政府形象雕塑师</h2>
						
						
																											<div id="postmessage_405149" class="t_msgfont">[提要] 伍皓，云南省委宣传部副部长，因在“躲猫猫”事件中召集网友实地调查，体现大众参与权，而走红。在今年各媒体的年度盘点中，他都列入其中。《新周刊》把他评选为“2009十大猛人”之首，《南方周末》列入年度人物之一。[我来说说伍皓]<br />
<br />
<img src="http://photocdn.sohu.com/20091229/Img269290519.gif" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
伍皓很忙，仅12月初的头一周他就两飞北京，并到哈尔滨、四川等地。图为伍皓在哈尔滨接受记者采访。<br />
<br />
<br />
<br />
&nbsp; &nbsp; 相关新闻：<br />
<br />
&nbsp; &nbsp; 对话伍皓：说我另类是因社会缺少讲真话之习惯&nbsp;&nbsp;<br />
<br />
&nbsp; &nbsp; 伍皓精彩语录：<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;我们在体制内，想做一件事情，并不是想怎么做就怎么。很小的事，都要写报告，层层申报，从科长处长一直批到高级领导。这个体制没有鼓励你去创造，而是执行，只做领导交待的事，做了没风险。<br />
<br />
&nbsp; &nbsp; 大家觉得我另类和大家不一样，那是因为全社会都还没有形成一个讲真话的习惯。<br />
<br />
&nbsp; &nbsp; 现在的领导也不都是昏官，你真正把事情做好，社会效果社会评价也不错，领导也是看得到的，也不至于为这些事情怎样。<br />
<br />
&nbsp; &nbsp; 伍皓忙得很，在12月初的头一周两飞北京，又从中国最北的哈尔滨，飞回昆明，这期间还到四川，欣赏了国际小姐的选美。事无巨细，他的行程都公布在自己的博客中，每个人都可以看见。<br />
<br />
&nbsp; &nbsp; 不光如此，这个出身山区苦地方的云南省委宣传部副部长，还在网上公开了自己的收入、住房与用车的情况。自觉如此，这在全国的官员中，实属罕见。<br />
<br />
　　以至今年的各大媒体的年终盘点中，《新周刊》杂志把他评选为“2009十大猛人”之首，《南方周末》列入年度人物之一，《都市快报》、《南风窗》等也列入评选。名利名利，名在利前。<br />
<br />
　　改革开放之后，求利已是国人所趋。而名，国人则一向讳言，俗话说闷声发大财。对自己名声的雀起，伍皓也感觉到了压力：“人出名后会有两种死法，不是‘棒杀’就是‘捧杀’。”<br />
<br />
　　“这些事都是易招人猜忌的，别人要去上级面前说，还不知怎么添油加醋呢。自己放到阳光下，有眼睛盯着不会让自己变坏，再听到小报告，上司也会说这事已知道，想进你谗言就没了机会。所以，我一向觉得个人事项报告不仅是廉政需要和公众期待，也是自我保护，何乐不为。”伍皓把公开与自我保护联系到了一起。<br />
<br />
　　网上公开家庭财产<br />
<br />
　　38岁的伍皓来自四川达县的农村，因成绩优异考上了北大。<br />
<br />
　　那是在80年代末，村里集资给他买了火车票送到北京，第一次离开家的他，一路上抱着包裹，觉也不敢睡。到了城市后，伍皓连斑马线都不认识。后来他自愿申请到西藏工作，从新华社西藏分社一名普通记者起步。<br />
<br />
　　“小时候去山上放牛，看书入了迷，天黑了牛不见了。幼小的妹妹去林子里帮我找牛，荆棘划着脸，破了相，至今留下伤痕。就这个妹妹，夫妻俩都下了岗，来昆明投靠我。我帮不了她什么。对妹妹，我亏欠太多。”伍皓博客中坦然回忆自己的艰苦童年，并晒出了自己的财产：私宅一套，老婆上班用私车一辆，月工资5000，偶尔有点讲课费之类的外快。原来开了十年的老旧“夏利”，送给了妹妹。<br />
<br />
　　在网上，他从家庭生活到工作理念无所不谈。甚至对共公事务，如“唐福珍自焚事件”、“上海钓鱼案”等敏感事情，他也批评说，某些人以为让自己的媒体为自己说话就是“引导舆论”，还说出了“唐福珍不幸没有生活在我们云南。”这样犯官场忌讳的话语来。<br />
<br />
　　同样的，媒体对自己的报道，伍皓也一样把他贴出来。“有人不理解我为什么老把媒体对我的报道晒出来，以为我超级自恋、沾沾自喜。”伍皓说，“害人之心不可有，防人之心不可无。自己不说，不定有谁又要拿这些报道去背后捅我一刀子呢。”<br />
<br />
　　对于伍皓网上晒自己，也有砸场子的网友，用身为官员，却不务正业来质疑伍皓。“这里是我私人的地方，想谈云南的公务去看云南的官方博客。”伍皓回应说。<br />
<br />
　　第三方调查已成处理网络事件的范本<br />
<br />
　　云南官方博客，是伍皓的新政之一。从组织“躲猫猫真相调查团”、亲自上网回帖，到建立网络新闻发言人等，云南一直是沿着伍皓“网络开放”的思路在前进。<br />
<br />
　　他的第一炮，当属躲猫猫事件中的“网友调查团”。虽然尽管成立仓促，争议非常大，也有很多缺陷，但伍皓还是得意于这个做法。“后来的全国的突发事件，都采用了第三方调查的方法，如钓鱼案，如徐宝宝。成为了一个范本。”<br />
<br />
　　2009年2月8日下午的云南省晋宁县看守所里时，却引发了一场悲剧：据当地警方说，因盗伐森林被拘押的24岁男子李乔明，和同监室的狱友在天井里玩“躲猫猫”游戏后身亡。舆论哗然，“躲猫猫”成为网络热词。<br />
<br />
　　要知道，当时伍皓刚刚到宣传部门三个月，还没有转正。但伍皓当时就认识到：“传统认为宣传部门更多的处在幕后。但现在时代的要求，传播环境与新闻环境发生了深刻的变化，我们要主动性宣传，变宣传为传播。”<br />
<br />
　　“我们在介入的时候，所有的网民对这个调查结论已经不相信了。”伍皓说，总得要找到一个破题的方法，我就想到了让网友自己去调查。体现大众的参与权，调查出什么结论，就发布什么结论，体现了大众的知情权。通过网友的参与表达，最终体现监督权。<br />
<br />
　　对于自己的做法理论来源，伍皓不忘强调其理论支持是来自党的十七大报告。“坚持国家一切权利属于人民，从各个层次、各个领域扩大公民有序政治参与，最广泛的动员和组织人民依法管理国家事物和社会事物，管理经济和文化事业。要保障人民的‘知情权、参与权、表达权、监督权’。”伍皓说，“不能中央讲这些，我们也跟着喊喊口号，就要探索实现这四个权利的途径，实现有序扩大的途径。”<br />
<br />
　　云南省委对伍皓的创新，显然表示了认同，在一年之后，伍皓成功转正，并在原来分管新闻的基础上，增加了管理网络舆论的职责。<br />
<br />
　　向网友的质疑精神学习<br />
<br />
　　对网民的只会质疑从不建设，伍皓有自己的观点：“多元多样多变，这个是社会发展的总的趋势，要求大家都要一致赞成与拥护，不可能，有不同的声音是正常的，而且是必须的，都一样才不正常。”<br />
<br />
　　伍皓认为这是互联网的特点。没有了就不是互联网了。“我说过很多次，谩骂无意义，质疑有价值”。他表示，不仅要容得下质疑，相反要从中学习质疑的精神与思维模式。<br />
<br />
　　“网上的智慧是无穷的，一个事情网民往往能从匪夷所思的角度去质疑，能让我们的决策更加科学与完善。”伍皓表示，政府提出决策，是进行可行性论证，组织各种专家来论证。但网民是反着来的，随你说的天花乱坠，他都能从中找到不可行的地方，所以政府的决策要学习网民的思维。怎么样做才能避免网民所说情况的发生。<br />
<br />
　　“我们提出的政策，在网民的监督下，总是有缺陷与漏洞。”伍皓最近提出媒体义务监督员，网民质疑这是新闻秘密警察，并问监督员谁来监督等问题。“这就提醒了我们，要规范监督员的一些行为，比如不能直接干预媒体的采访。”<br />
<br />
　　伍皓介绍，每一次尝试后，他们在进行总结评价判分析，哪种手段更加符合现在传播环境，更为网民所接受。“到目前为止，我们一步一步最终都是举一反三，我们解决一个问题；总结一个经验；然后就形成一个制度。都是按照这种方法来做的。去年一年我们出台的成文的制度就达到20多条，假新闻的社会公示制度，网络新闻发布会的制度。只有制度化的东西才能是留存下来的东西，不以人员的变化而变化，比如我个人的变动而变动或者是后退。”<br />
<br />
　　把宣传部门变成“形象塑造师”<br />
<br />
　　网上对伍皓本人的质疑声中，最刻薄的批评要算了“伍皓，字毛。”。直指伍皓在立场上还是完全站在公权力一方。对此，伍皓表达知道有人这样讲自己，不过他也不在意。“自信的政府，自信的官员，要容得下批评。而且媒体的公开报道中，还都是理性支持我们的创新。”<br />
<br />
　　“广东话‘皓’和‘毛’同音。我本身就是宣传部门的人，我怎么能聘我自己来当五毛。”对于自己的立场，伍皓表示首先是要履行职责。“我首先是一个宣传部的副部长，我是党委政府的形象雕塑师，我肯定是要维护党委和政府的形象，这个我从来也不避讳。但是怎么才叫维护了党委政府的形象，有些人理解是把事情捂起来，家丑不外扬，就叫维护政府和党委的形象。” 伍皓坦言，无数事实证明，一些做法不但没有维护形象，反而使政府的公信力受到极大的伤害。“周老虎事件中，网民与当地政府进行了八个月的较量，这个较量的过程中，政府的公信力一天天的流失，根本起不到维护的作用。”<br />
<br />
　　“而我的理解是要维护公民的知情权，要把事实的真相告知需要知道真相的人，帮助公民接近真相，探寻真相，了解真相，这样才能真正维护党委政府的形象，阳光政府，法制政府，责任政府，这才是党委政府应该树立的形象。”伍皓说。（拾年）　　<br />
