																											<div id="postmessage_314587" class="t_msgfont">北大医院“学生治死教授”案将二审<br />
<br />
2009年11月05日08:21<br />
<br />
　　公益医院非法行医 北大医学教授惨死北大医院<br />
<br />
　　中广网北京11月5日消息 死者叫熊卓为，是北京大学的一位医学教授，在北京大学第一医院心血管研究所任研究员，去世时只有49岁。<br />
<br />
　　熊卓为的丈夫王建国是北京大学光华管理学院的教授。据他回忆，2005年12月，熊卓为因长期伏案工作，有些腰疼，到北大第一医院拍片之后发现腰椎出现轻度滑脱。北大第一医院骨科主任李淳德诊断需要尽快手术。“他（李淳德）跟她讲4天就可以下床，一下就可以恢复。”但术后，熊卓为并没有像医生说的那样很快恢复，术后第七天，北大第一医院宣布，熊卓为因发生术后并发症肺栓塞，抢救无效死亡。<br />
<br />
　　妻子只不过是做了一个小小的骨科手术，怎么就会意外死亡呢？熊卓为的同学，现任北京阜外心血管医院院长胡盛寿在熊卓为陷入昏迷的最后阶段参与了抢救。“他出来就告诉我，救得太晚，没有救了。他说有几道关卡，任何一道如果堵住了都不会死。”<br />
<br />
　　在校学生无证行医<br />
<br />
　　王建国难以理解胡盛寿所说的几道关卡的含义。随后的调查，不仅让他震惊，更让他愤怒。对于熊卓为的死亡，北大第一医院的结论是，手术后并发症肺栓塞，抢救无效死亡。而王建国在妻子的病例记录上看到，妻子的肋骨折断了3根，心脏、肝脏全都破了。为什么会这样？从手术记录中，王建国发现，是医生在抢救时按压造成的。<br />
<br />
　　王建国的诉讼代理人卓小勤说：“抢救过程非常粗暴，造成肋骨胸骨骨折，然后刺破心包，刺破心脏，同时又造成肝脏破裂，最终还是由于肝脏破裂大出血无法止血而放弃抢救。”<br />
<br />
　　在调查中，王建国和律师发现了一件让他们难以置信的事――负责观察、诊疗、抢救的主治医生段鸿洲、于峥嵘和肖建涛竟然都是没有行医资格的北大医学院的在校学生。而最后的死亡证明，也是于峥嵘开的。<br />
<br />
　　记者随后查阅了《中华人民共和国执业医师法》，第14条规定：未经医师注册取得执业证书，不得从事医师执业活动。<br />
<br />
　　王建国告诉记者，对于于峥嵘等3名没有医师资格的学生行医的事实，北大第一医院并没有否认。但是院方强调，病人死于术后并发症肺栓塞，跟学生行医没有直接关系，医院对此无需承担责任。<br />
<br />
　　死者病例多处被修改<br />
<br />
　　王建国在妻子刚去世不久，就从北大第一医院复印了病例。当他准备和北大第一医院对簿公堂时，发现病例当中多处都被修改了。妻子的肋骨断了3根，这一点竟然在病例中消失了。<br />
<br />
　　王建国和律师统计，病例中被修改的地方不下10处。在被修改得面目全非的病例中，死者的死亡时间竟然有3个：临时医嘱单上记录，1月31日的3 点30分，尸体处理一次，显示病人已经死亡；死亡志里记录的是4点50分，抢救无效呼吸心跳停止；在证明熊卓为死亡的心电图上，心跳呼吸停止的时间则是6 点53分。病例中多处修改涉及专业医疗知识，他们反复请多名医学界的朋友帮助分析查找，才得以发现其中的玄机。<br />
<br />
　　判定诊疗与死亡有因果关系<br />
<br />
　　2009年7月1日，在熊卓为死亡3年7个月之后，西城区法院一审作出了判决，北大第一医院的诊疗跟熊卓为的死亡有因果关系。拿到判决书后，王建国号啕大哭。<br />
<br />
　　记者在采访中发现，熊卓为在北大第一医院所遭遇的这一切，并不是个例。北京市律师协会医疗法律专业委员会秘书长孙万军告诉记者，他代理的案件当中有两例是有关北大第一医院医生无证行医的。按照北大第一医院的规定，见习医生都是跟着上级医生观摩学习，绝对不会独立看病。记者以患者身份在北大医院急诊挂了一个外科号。在急诊科，医生简单问了一下病情之后，给记者开了一些消炎药。医生名字叫刘希高，记者在卫生部公布的执业注册医师查询系统进行了查询，发现系统里面没有任何关于刘希高的注册信息。<br />
<br />
　　记者注意到，急诊办公室的桌子上有张值班医生表，记者随后把相关医生名字输入卫生部执业医生查询系统进行查询，发现除了刘希高以外，李少雷、周怡君也同样没有任何注册信息。而在熊卓为曾经动手术的北大第一医院骨科，记者在值班医生名字里面看到了于峥嵘的名字，他正是当初给熊卓为看病的在校学生，现在他已经是北大医院骨科的主治大夫。 《经济半小时》供稿<br />
<br />
　　卫生部：医院实习生不算“非法行医”<br />
<br />
　　近日有媒体报道北京大学第一医院许多无执业医师执业证书的医学生“非法行医”，引起社会各界的广泛关注。卫生部高度重视此事并于昨日作出相关回应。<br />
<br />
　　据悉，卫生部已责成北京市卫生局对此事进行认真调查，核清事实，提出处理意见，并及时向社会公布。<br />
<br />
　　卫生部指出，医学临床学习和实践活动是医学教育教学的重要组成部分。每一个临床医生走上工作岗位之前，必然需要经历一段时间的医学临床学习和实践过程。为保护患者、教师和学生的合法权益，保证医学教育教学质量，并最终保障人民群众获得安全可靠的医疗卫生服务，按照执业医师法和高等教育法的有关规定，卫生部和教育部对医学生和试用期医学毕业生从事临床实践活动制定了明确的管理要求。<br />
<br />
　　两部门联合印发的《医学教育临床实践管理暂行规定》规定，“医学生在临床带教教师的监督、指导下，可以接触观察患者、询问患者病史、检查患者体征、查阅患者有关资料、参与分析讨论患者病情、书写病历及住院患者病程记录、填写各类检查和处置单、医嘱和处方，对患者实施有关诊疗操作、参加有关的手术”；“试用期医学毕业生在指导医师的监督、指导下，可以为患者提供相应的临床诊疗服务”；“医学生和试用期医学毕业生参与医学教育临床诊疗活动必须由临床带教教师或指导医师监督、指导，不得独自为患者提供临床诊疗服务。临床实践过程中产生的有关诊疗的文字材料必须经临床带教教师或指导医师审核签名后才能作为正式医疗文件”。<br />
<br />
　　卫生部表示，严格意义上说，医学生和试用期医学毕业生在医疗机构的临床实践活动是医学教育中的临床实践活动，而非正式的行医。对这种活动的管理，是有法可依，有章可循的。我国刑法对于“非法行医”的具体界定，不包括医学生和试用期医学毕业生从事临床实践活动。<br />
<br />
　　今日二审，所判赔偿将全部捐赠<br />
<br />
　　媒体报道由于北京大学第一医院非法行医，造成北京大学医学教授熊卓为惨死于手术室，引起各界广泛关注。不过北大第一医院11月3日公开回应指出，有关该院“非法行医”的报道，内容失实、断章取义。<br />
<br />
　　而熊卓为的丈夫、同为北京大学教授的王建国，昨日在接受记者采访时表示，今天下午北京市高级人民法院将对案件进行二审。至于医院“非法行医”，王建国表示，有三条铁证如山的证据：1，北京市卫生监督所已经查证于峥嵘、段鸿洲和肖建涛为违法行医；2，在卫生部网上可以查证这三位在校生的医生注册时间均在2006年1月31日之后；3，在熊教授病历上这三位违法行医者进行的医疗活动没有执业医生签字。<br />
<br />
　　王建国表示，一审结果整体来讲是公正的，但自己“既满意又不满意”：满意的是法院认定妻子惨死的责任在医院；但也有不满，包括一审判决书中并未列明医院非法行医、病例作假，这些都已由北京市卫生监督所认定，相关文件也作为证据呈交法庭。<br />
<br />
　　王建国说，自己这几年来一直坚持打官司，目的就是“要救人”，希望妻子的惨死，能警醒医院对管理制度的改善，能唤醒医生对病人生命珍视的良心和责任感，由此而挽救更多无辜的生命。至于赔偿金，只是希望给生命的价值一个公正的评价。王建国说，妻子从海外归国，自费添置了40多万元人民币的科研设备，如今已全部赠送给北京大学；同时无论法院最终判定多少赔偿，他都将以妻子的名义捐赠给有需要的人，“如果我的努力能够避免更多类似的惨剧发生，我觉得我的付出是值得的”。<br />
<br />
