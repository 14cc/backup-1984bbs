　　熊卓为只是做了一个骨科手术,为什么肋骨断了,心脏破了,肝脏也破了呢？从手术记录中,王建国发现,是医生在抢救的时候按压造成的。<br />
<br />
　　诉讼代理人卓小勤:“抢救过程当中是非常非常地一个粗暴的一个抢救,然后造成肋骨胸骨骨折,然后刺破心包,刺破心脏,同时又造成肝脏破裂,最终还是由于肝脏破裂大出血无法止血而放弃抢救。”<br />
<br />
　　王建国:“三厘米宽、八厘米长是个什么口子你可以想象得到,那样冒血那时候人还有救吗？”<br />
<br />
　　熊桌为到底是因为什么原因死亡的呢？王建国和律师在调查的过程中,发现了一件让他们难以置信的事。<br />
<br />
　　诉讼代理人卓小勤:“凡是病例当中出现的医生和护士我们都去上网去查他的资质,这样我们发现,段鸿洲、于峥嵘和肖建涛是没有资质的。”<br />
<br />
　　记者在熊卓为的病历记录上看到,负责观察,诊疗,抢救的主治医生叫段鸿洲、于峥嵘和肖建涛,最后的死亡证明,也是于峥嵘开的。而王建国告诉记者,这三个人,竟然都是没有行医资格的北大医学院的在校学生。记者随后查阅了《中华人民共和国执业医师法》,第十四条规定:未经医师注册取得执业证书,不得从事医师执业活动。记者感到匪夷所思的是,北大第一医院是全国知名的好医院,其技术水准和规范管理一直是国内医院的榜样,这样公然违法而且置患者权益于不顾的行为,怎么可能发生在这里呢,更何况,病人还是本医院的一位知名教授？<br />
<br />
　　王建国:“这种大医院,这是全国最好的医院之一,怎么可以出,出这种ABC,这是犯的一种最低级最ABC的一种错误,你去想,一个人的生命去交给一个没有资质的人这是什么意思？这就好比我把我自己交给街上一个人要他给我开刀是一样的,随时都可能损失生命。”<br />
<br />
　　王建国告诉记者,对于于峥嵘等三名没有医师资格的学生行医的事实,北大第一医院并没有否认,但是院方强调,病人死于术后并发症肺栓塞,跟学生行医没有直接关系,医院对此无需承担责任。为了搞清妻子的死因,王建国在妻子刚去世不久,就从北大第一医院复印了病例,当他准备和北大第一医院对簿公堂的时候,他发现病例当中多处都被修改了。妻子的肋骨断了三根,这一点竟然在病例中消失了,而且院方还把熊卓为当初自己走着进医院的情况改写成了坐轮椅入院。<br />
<br />
　　王建国:“我们有很多人一起,包括她的学生还有同事,提着袋子说说笑笑走到医院去,她这个诬蔑说,我太太是坐着轮椅推过去的,还威胁她的学生,要学生和他们的职员造假,说她是坐轮椅去的。”<br />
<br />
　　王建国和律师统计,病例中被修改的地方不下10处。<br />
<br />
　　诉讼代理人卓小勤:“恶意的一个篡改病例,这是医生做的手术记录,他是把一度滑脱改为二度滑脱,而且修改的痕迹是非常明显的。”<br />
<br />
　　王建国发现,病例中多处修改涉及专业医疗知识,他们反复请多名医学界的朋友帮助分析查找,才得以发现其中的玄机。<br />
<br />
　　诉讼代理人卓小勤:“那么为什么要改这个,如果是一度滑脱是没有手术适应症也就不应该做这个手术的,所以我们认为,是医生认为就是术后发现出了问题了,怕担责任,就试图把术前诊断由一度改为二度。”<br />
<br />
　　术后第六天,熊卓为陷入昏迷,抢救过程中,北大第一医院从她的体内取出了血栓,,王建国当时就在现场,他看到取出的血栓是白色的,但是后来北大第一医院所出具的血栓化验单上写的却是红色血栓。<br />
<br />
　　王建国:“说她是红色血栓,因为没有抗凝导致的血栓,一般来说它是混合血栓,如果是红色血栓的话,那就说你即使不抗凝也没有问题。”<br />
<br />
　　王建国发现,北大医院试图摆脱责任的这张血栓化验单,在时间上根本不成立。<br />
<br />
　　王建国:“因为我的太太是1月31号凌晨取的血栓,它的化验单是1月29号。”<br />
<br />
　　在被修改的面目全非的病例中,王建国他们发现了很多前后矛盾的疑点,比如,死者的死亡时间竟然有三个。临时医嘱单上记录, 1月31号的上午3点30分,尸体处理一次,显示病人已经死亡,但是在死亡志里边记录的则是上午4点50分,抢救无效呼吸心跳停止,而在证明熊卓为死亡的心电图上,心跳呼吸停止的时间则是6点53分。<br />
<br />
　　王建国:“这叫什么事,这都是奇闻了,这么好的医院,你怎么会高出这种事来。”<br />
<br />
　　没有行医资格的学生给病人看病,病人死亡后,修改病例,这样的情况发生三级甲等医院,听起来确实像奇闻。<br />
<br />
　　王建国认为,这些没有行医资格的学生缺少从医经验,在许多环节上处置不当,人为事故层出不穷,院方为了隐瞒真相,不得不事后修改病例。<br />
<br />
　　比如说熊卓为是一位高血凝患者,手术过程中容易引发血栓,术后需要服用抗凝药物加以预防,但王建国发现,北大第一医院的学生医生们并没有给熊桌为采取任何抗凝措施。<br />
<br />
　　北大教授熊卓为丈夫王建国:“（手术后）两天内,我的太太这个脚就开始胀,这个底脚就开始疼,如果按照现在看,她实际上当时已经在发生血凝,她是栓在（神）静脉血栓了,已经在,已经在脚上了,但是当时我们问这个医生,他说这个那个伤口神经痛。”<br />
<br />
　　王建国回忆,做完手术后的第三天,熊卓为腿部肿胀的厉害。<br />
<br />
　　王建国:“他们不仅仅没有给她检测,还给她吃大量的大止痛药,这个止痛药可以掩盖,掩盖这个血栓疼痛的这个表征,栓了还不知道疼。”<br />
<br />
　　手术后的第五天,熊卓为开始呼吸困难,当天不得不吸氧六小时。<br />
<br />
　　王建国:“后来才知道,这时候已经血栓已经很严重了,它说不一定已经跑到肺里边这些地方来了,因为她呼吸不畅,那肺肯定受到影响,但他们还是没采取措施。”<br />
<br />
　　2008年年底,律师的一个偶然发现,让王建国发现了妻子死亡背后这些不为人知的秘密。北大医院麻醉科主任公开发表了一篇文章,分析忽略抗凝导致死亡的严重性,作者引用的这起死亡事故的主人公,碰巧就是熊卓为。<br />
<br />
　　王建国:“这篇文章里边清清楚楚地讲了,她的肺栓塞的发生,讲的这是高危病人,容易发生肺栓塞,他们没有采取任何抗凝的措施,第二他这个里面也证明了一点,这个肺,两个肺动脉并没有完全栓死,只栓了一个,另外一个还可以流通,那时候抢救是可以抢救得了的,如果当时不把心脏按破,不把肝脏按破,把那个肺栓一取掉她根本不会死。”<br />
<br />
　　北京阜外心血管医院院长胡盛寿参与了熊卓为最后的抢救,王建国在妻子去世后,和胡盛寿通了一个电话,把胡盛寿对于妻子死亡原因的判断录在了手机上。<br />
<br />
　　阜外心血管病医院院长胡盛寿:“这个整个给她做完手术以后,术后的处理常规有问题,他没有给她抗凝治疗,明明一个高凝状态,做骨科手术长期卧床,不给她吃抗凝药,这是直接导致她肺栓塞形成的原因,抢救的过程当中,又使用非医疗人员进行抢救,导致抢救失败。”<br />
<br />
