													<h2>人民网P中央台 教授死亡引发党媒内讧</h2>
						
						
																											<div id="postmessage_316144" class="t_msgfont">教授之死折射新闻浮躁<br />
白剑峰<br />
2009年11月06日08:12&nbsp;&nbsp;来源：人民网-卫生频道<br />
<br />
 【字号 大 中 小】&nbsp; &nbsp; &nbsp; &nbsp; 打印&nbsp; &nbsp; &nbsp; &nbsp; 留言&nbsp; &nbsp; &nbsp; &nbsp; 论坛&nbsp; &nbsp; &nbsp; &nbsp; 网摘&nbsp; &nbsp; &nbsp; &nbsp; 手机点评&nbsp; &nbsp; &nbsp; &nbsp; 纠错&nbsp; &nbsp; &nbsp; &nbsp; E-mail推荐:&nbsp;&nbsp;<br />
<br />
　　日前，<strong>某著名电视台</strong>播出了《公益医院非法行医，北大医学教授惨死北大医院》的节目，称“在校学生无证行医导致死亡事故”。记者用大量生动的“事实”，证明了北大医院存在严重的“非法行医”现象，是一家“草菅人命”的医院。<br />
<br />
　　与几年前的哈医二院“天价药费”相比，这条新闻的轰动性毫不逊色。但是，看过节目之后，有人义愤填膺，也有人质疑追问：事实真的如此吗？<br />
<br />
　　众所周知，真实、客观、公正、平衡，是新闻的生命所在，也是媒体的公信力所在。一篇报道能不能经得起历史的检验，关键是看其基本事实是否站得住。从这则报道来看，很多事实都有“硬伤”。<br />
<br />
　　例如，该报道称，医学生于峥嵘在参与抢救熊教授时，没有行医资格，属于“非法行医”。这也是整个报道的核心事实和最大“卖点”。但是，记者的调查显然不够扎实，导致报道严重失实。根据北大医院在法庭上出具的证据，在给熊教授诊疗期间，于峥嵘已经于取得了《医师资格证书》，发证日期是2005年12月16日，但其《医师执业证书》当时尚在办理中。根据卫生部规定，“已取得《医师资格证书》，并具备申请执业医师注册条件的医师，非本人原因导致未获得《医师执业证书》前，在其受聘的医疗预防保健机构和工作时间内的执业活动不属于非法行医。”这就是说，教授之死与“非法行医”没有任何关系。<br />
<br />
　　新闻播出后，卫生部也给予回应：医学生和试用期医学毕业生在医疗机构的临床实践活动是医学教育中的临床实践活动，而非正式的行医。对这种活动的管理，是有法可依、有章可循的。我国刑法对于“非法行医”的具体界定，不包括医学生和试用期医学毕业生从事临床实践活动。卫生部和教育部明确规定：“试用期医学毕业生在指导医师的监督、指导下，可以为患者提供相应的临床诊疗服务”。事实上，即便是在西方发达国家，很多医院里也有医学实习生，他们可以协助医生从事临床活动，如书写病历、填写各类检单、对患者实施有关诊疗操作、参加手术等。显然，这是医学教育的特殊规律。而北大医院的医学生参与抢救熊教授，也都是在医生的指导下进行的，没有任何违法之处。<br />
<br />
　　又如，央视记者引用患者家属的说法，认为医生没有用抗凝药物，导致病人术后发生肺栓塞并发症。其实，病人术后该不该使用抗凝药，是个纯学术问题，目前骨科学界尚存不小争议。抗凝药是把双刃剑，不使用可能肺栓塞，使用了可能大出血，很难有绝对的结论。在临床上，骨科病人由于长时间卧床，发生深静脉血栓的概率很高，而深静脉血栓又极易引发肺栓塞。而一旦发生肺栓塞，抢救成功率极低。这说明，医学是一门具有复杂性和局限性的科学，只能在探索中爬行。很多病情的发生是不以人的意志为转移的，有的连医学专家也难以预料，或者无能为力。因此，苛求医生的诊治没有任何疏漏，不符合客观规律，无可指责。<br />
<br />
　　央视记者还引用了另一个说法：抢救过程非常粗暴，病人先是肋骨胸骨骨折，然后刺破心脏，同时又造成肝脏破裂，最终由于大出血而放弃抢救。这显然歪曲了事实。稍有医学知识的人都知道，心肺复苏术一般适用于心跳停止的病人。这至少说明，先有病人心跳停止，后有医生按压胸廓，而这也是绝地求生的唯一正确选择。事实上，心肺复苏时按压胸廓很容易造成肋骨骨折，而肋骨骨折又会损伤脏器。但是，心肺复苏的效果很大程度上取决于按压力度，力度越大，心脏泵血越好，但对胸廓的损伤也越大。在紧急抢救中，这样的做法完全符合医学规范。<br />
<br />
　　在这则新闻中，还有一个值得注意的现象：记者使用了很多主观性很强的字眼，例如“离奇死亡”、“蹊跷”、“屡伤人命”等，令公众不寒而栗。这样的词句虽然吸引“眼球”，但毕竟不如客观描述更有说服力。因为新闻的最高境界是用事实说话，而非渲染煽情。同时，记者似乎忘记了新闻的“平衡原则”。在医患双方的纠纷中，记者从头到尾都在引用了患方的证词，而缺乏对于医方的采访，尤其是没有医院关键当事人的说法。无论何种原因，这都使报道的客观性大打折扣。之所以出现这些“硬伤”，原因是多方面的。有的是因为记者调查采访不深入，有的是因为听信了一面之辞，有的是因为缺乏医学常识，还有的则是出于个人的主观臆断。从根本上说，这与记者的新闻素养有关，更与媒体的浮躁之风有关。<br />
<br />
　　近年来，我国新闻界出现了一种不良倾向。很多媒体一味追求“吸引眼球”，盲目迎合受众需求，甚至靠不断增加“负面新闻”来提高收视率、收听率和阅读率。其后果是，记者不再靠事实引导公众，而是靠情绪引导公众；媒体不再忠于事实真相，而是忠于公众的情绪。这种错误的新闻价值观，助长了记者的急功近利，催生了大量的失实报道，“纸馅包子”就是一个沉痛教训。<br />
<br />
　　我国的医疗卫生领域问题很多、弊端很多，自然成了媒体关注的焦点。从积极的角度看，媒体开展<br />
<br />
　　舆论监督，揭露丑恶现象，有利于推动医疗卫生体制改革，维护广大群众的利益。但是，舆论监督的出发点应该是建设性的，出于善意、出于诚意、出于公心、出于良心，其目的是推动社会进步，促进医患和谐。如果把舆论监督仅仅当成“吸引眼球”的手段，故意制造“轰动效应”，就会使媒体丧失公信，记者丧失良知，最终成为历史的耻辱标记。</div>

							
							
							
							
