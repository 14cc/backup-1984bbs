<br />
　　以前我们听说过黑诊所里的非法行医,但是发生在公立医院,尤其是北大第一医院这样全国都有名的三级甲等医院,确实让人想不到。<br />
<br />
　　我们的记者多次要求采访北大第一医院,都被拒绝了。那么非法行医这种情况是否真的存在呢？我们的记者以患者的身份前往北大第一医院进行了暗访。<br />
<br />
　　北大医院办公室主任:“不会,绝不会,因为他们没有处方权。”<br />
<br />
　　北大医院办公室拒绝了我们的采访,不过对于见习医生开处方的情况,医院办公室的龙主任表示绝无此事。<br />
<br />
　　记者:“实习医生您确定不会开方子,不会参与诊疗活动。”<br />
<br />
　　北大医院办公室主任:“肯定不会。”<br />
<br />
　　这位龙主任表示,按照北大第一医院的规定,见习医生都是跟着上级医生观摩学习,绝对不会独立看病。事实是不是这样？记者随后以患者身份在北大医院急诊挂了一个外科号。<br />
<br />
　　医生简单问了一下病情之后,给记者开了一些消炎药。记者注意到,这位在门诊看病的医生名字叫做刘希高。那么刘希高是不是已经取得医师执业资格的医生呢？记者在卫生部公布的执业注册医师查询系统进行了查询。发现,系统里面没有任何关于刘希高的注册信息。<br />
<br />
　　记者:“您现在在这里工作？”<br />
<br />
　　北大第一医院急诊医生刘希高:“我在这里读博,在北大医院读搏,还在博士阶段,毕也不一定留下来。”<br />
<br />
　　记者:“还没毕业,哪年毕业？”<br />
<br />
　　刘希高:“还有两三年。”<br />
<br />
　　记者得知,按照执业医师法的规定,医生必须要取得执业注册资格才能行医。而急诊科通常接诊的是急重病人,所以对的医生要求更高,一般要求在临床工作5年以上才可以到急诊科看病。而刘希高只是一名差三年才毕业的在校博士生,为什么就可以来急诊科给病人看病呢？<br />
<br />
　　刘希高:“平时在病房里,只是3个月的急诊,急诊结束了就回病房了,培养模式就是让你当住院医,这是给你的待遇。”<br />
<br />
　　刘希高说,只要考入北大医院研究生,就可以享受住院医待遇,每月1000多元工资,医院管吃住,更重要的是,可以直接参与临床实践。<br />
<br />
　　记者:“回病房还有机会接触临床？”<br />
<br />
　　刘希高:“对阿,每天都要放病人,上手术,每天都有手术,我们所有的时间都在医院,上课就是周末。”<br />
<br />
　　记者:“你做过多少手术了？”<br />
<br />
　　刘希高:“我做过小的,阑尾炎什么。”<br />
<br />
　　记者注意到,急诊办公室的桌子上有一张值班医生表,记者随后把相关医生名字输入卫生部执业医生查询系统进行了查询,发现除了刘希高以外,李少雷,周怡君,也同样没有任何注册信息。记者随后来到了熊卓为曾经动手术的北大第一医院骨科。在值班医生名字里面,记者意外地的看到了于峥嵘的名字。他正是当初给熊卓为看病的在校学生。我们了解到,按照刑法336条规定,未取得医生执业资格非法行医,情节严重的,处三年以下有期徒刑,严重损害就诊人身体健康的,住处三年以上十年以下有期徒刑,造成就诊人死亡的,处十年以上有期徒刑。于峥嵘非法行医早已经法院认定,不过记者从值班护士那里了解到,于峥嵘似乎没有受到这件事情的影响,现在他已经是北大医院骨科的主治大夫,这一周跟随主任到泰国学习去了。<br />
<br />
　　记者:“你们说的是于峥嵘大夫是吧。”<br />
<br />
　　北大医院骨科护士:“对,他现在是我们科的大夫了,就是主治大夫。”<br />
<br />
　　护士告诉我们,现在住院处的主力医生,有不少就是在校的学生。在医生值班室,我们遇到了一位正准备上手术台给病人做手术的北大医学院的学生。<br />
<br />
　　记者:“刚才那小伙子考执业资格证吗？”<br />
<br />
　　北大医院骨科实习生:“他应该是明年就可以考。”<br />
<br />
　　记者:“但他今年就已经在做临床的工作了。”<br />
<br />
　　北大医院骨科实习生:“对。”<br />
<br />
　　记者:“医院允许这么做？”<br />
<br />
　　北大医院骨科实习生:“对,在这儿实际上可以,就是收收门诊过来的住院病人,看病例,开遗嘱,上手术。”<br />
<br />
　　半小时观察:谁来保护我们的看病权？<br />
<br />
　　看到我们记者的暗访,尤其是最后看到那位还没有考取医师资格的学生正在准备去做手术,我实在不知道该说什么了,我真是很担心那位躺在手术台的患者。<br />
<br />
　　今天,我也特意上了一下北大第一医学院的官方网站,北大第一医院创建于1915年,是我国最早创办的国立医院,网站自称该院学科齐全,综合诊疗水平高,承担着北京全市6万多人的公费医疗,以及占北京八分之一的干部保健任务。就诊患者中,有1/3是来自全国各地的疑难病患者。北大第一医院自称其医院文化是厚德尚道,但国家的法律法规清清楚楚,没有职业资格不得行医,我不明白像北大第一医院这样历史悠久、国内外知名的医疗机构为何敢公然违法,而且在事故屡屡发生之后依然不改？国家和北京市的卫生监管部门为何在这里失控？这样一家知名的医院都不遵守国家法律、不尊重患者生命权利,不仅让人震惊,更让人寒心,全国到底还有多少类似的医院在草菅人命？我们期待尽快得到答案,究竟谁在保护我们的看病权？<br />
