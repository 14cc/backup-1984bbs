<br />
　　王建国:“她现在的骨灰还放在这个上面,我不敢去埋,我把它放在八宝山,我做一个梦,她说她要回来,我就给她从八宝山把她接回来了,有的一些时间没做梦,我就去磕头,去求她。”</div>

							
							
							
							
