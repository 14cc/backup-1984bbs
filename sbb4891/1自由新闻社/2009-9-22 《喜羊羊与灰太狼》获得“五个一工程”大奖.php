																											<div id="postmessage_252276" class="t_msgfont">昨天上午，中宣部召开第十一届精神文明建设“五个一工程”表彰座谈会，广东省喜获“五个一工程”优秀作品六项大奖。这是广东在近几届“五个一工程”评选中获优秀作品奖数量最多的一次。<br />
<img src="http://img1.cache.netease.com/cnews/2009/9/22/200909220429043a73f.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
广东获奖名额近年最多<br />
<br />
由中宣部组织的精神文明建设“五个一工程”评选活动自1992年起每年进行一次，评选上一年度精神产品中五个方面的精品佳作。这五个方面是：一部好的戏剧作品，一部好的电视剧（片）作品，一部好的图书（限社会科学方面），一部好的理论文章（限社会科学方面）。1995年度开始将一首好歌和一部好的广播剧列入评选范围，“五个一工程”名称不变。<br />
<br />
本届“五个一工程”评选取消了特等奖和入选奖，仅保留优秀作品奖，增加了电视动画片门类。名额从上届268部降到162部。广东省获奖名额却为近年之最，有6部作品获奖。上届广东省有4部作品获优秀奖：电影《我的法兰西岁月》、电视剧《亲情树》、歌曲《永远的小平》、图书《护士长日记：写在抗非典的日子里》。<br />
<br />
新增“电视动画片”门类<br />
<br />
令广州市民欣喜的是，本届评选新增“电视动画片”门类，穗产动画片《喜羊羊与灰太狼》一击即中。《喜羊羊与灰太狼》讲述青青草原上羊村里的喜羊羊、美羊羊、懒羊羊等羊儿们与灰太狼斗智斗勇的一连串充满童趣的故事。<br />
<br />
此前，该作品已先后荣获“金龙奖最佳创意动画奖”、“金鱼奖最佳游戏画面奖”、“全球华人‘非常短片’创意大赛奖”、“白玉兰银奖”等国内外众多奖项。</div>

							
							
							
							
