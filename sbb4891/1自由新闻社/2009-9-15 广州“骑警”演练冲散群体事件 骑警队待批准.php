													<h2>广州“骑警”演练冲散群体事件 骑警队待批准</h2>
						
						
																											<div id="postmessage_241554" class="t_msgfont">来源：网易 <a href="http://2010.163.com/09/0915/08/5J86P0TK00863AUC.html" target="_blank">http://2010.163.com/09/0915/08/5J86P0TK00863AUC.html</a><br />
<br />
数十名男子扮演“示威者”，手持矿泉水瓶不断叫喊。指挥人员一声令下，八匹骏马齐头并进，冲向“示威人群”，人群顿时被骏马从中间分开，其后压阵的警车迅速跟上，配合持盾牌的民警，控制住整个“示威人群”。这是骑警处理群体性事件演练的一幕。<br />
<br />
南方网9月15日报道 一边是大批手持矿泉水瓶的“示威者”，一边是手持警式盾牌的民警和八匹体型矫健由训练人员驾驭的骏马，随着指挥人员的一声口令，“嗒、嗒”的马蹄声响起，训练人员骑着骏马冲散“示威人群”。<br />
<br />
<img src="http://img4.cache.netease.com/sports/2009/9/15/2009091508251514fef.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
骑警冲散“示威人群”<br />
<br />
这是警方处理群体性事件演练的一幕。警方知情人士表示，目前广州某区公安分局的民警正在接受骑术训练，但演练仍在试验阶段，骑警队何时将被批准成立以及所属建制、职能等均未有具体规划。<br />
<br />
八马打头警车压阵<br />
<br />
昨日下午4时许，萝岗区黄陂新村附近，两辆警车开道，训练人员手持十字警棍、脚穿长靴骑着八匹深色骏马，缓缓步向演练场地，所到之处，引起不少居民驻足围观。<br />
<br />
在演练现场，数十名男子扮演“示威者”，手持矿泉水瓶不断叫喊。另一边，20名手持警式盾牌的民警一字排开，双方相互“对峙”。民警身后，训练人员骑着八匹高头大马，两辆防暴警车则最后压阵。只见一名指挥人员手持扩音器，一声令下“开始”，持盾牌民警快速分开，八匹骏马齐头并进，整齐划一，冲向“示威人群”，“嗒、嗒”的马蹄声响起，人群顿时被骏马从中间分开，其后压阵的警车迅速跟上，配合持盾牌的民警，控制住整个“示威人群”。<br />
<br />
“马冲过来时，我的确感觉很害怕。”一位“示威者”说。训练人员说，一般人对马匹有恐惧感，8匹马整齐前进对冲散人群很有用。<br />
<br />
成立骑警队仍待批准<br />
<br />
训练人员介绍，当日演练中，只有5匹马是由民警骑着，另3匹马是由马术俱乐部的骑师骑着的。训练主要是针对“驱赶大规模人群”，目前已演练了两个多星期。<br />
<br />
该人员表示，由于每个人的“马感”不同，掌握骑术的时间长短不一，一般经过10节课的培训，大约1-2周就可以掌握骑术的技巧，预计将会训练30位民警和10匹马。“不过这些设想尚存在变化，具体的东西不好说。”<br />
<br />
负责马术训练的广州天麓骑术俱乐部负责人表示，“广州将要成立骑警队伍”这种说法还不太准确，一切事宜有待上级部门的批准，现在的训练还处于实验性的阶段。<br />
<br />
相关新闻:马匹对示威人群有威慑力<br />
<br />
目前，英国、美国、加拿大等20余个国家均设有专门的骑警队。在我国，宁夏、大连、深圳等地也都设有骑警队。<br />
<br />
据资料显示，骑警队在控制和驱赶大规模人群时能发挥独特的作用，一个警官骑在500公斤的马上能对骚乱人群形成极大的威慑力，一个骑警能抵得上10个步行的警察，这是很经济的，对骑警本人来说也很安全。警马的高度使警官具有高可见度，无障碍的视野使他们能监视几百米范围内的活动。骑警还能看到停放的车辆和过往车辆的内部情况，以及商店及商业活动的情况。<br />
<br />
此外，在不适宜用马的那些重大场合，骑警队也能变换战术，迅速改换工具，到达城市的任何部位。<br />
(本文来源：南方网 ) 果冻</div>

							
							
							
							
