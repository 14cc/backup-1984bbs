																											<div id="postmessage_782492" class="t_msgfont">泉州事情局长：国外预测我国事情必须按照我国法律进行<br />
[转]http://www.qzdzj.gov.cn/zhuanti/zt2008060401/1.htm<br />
　<br />
一、什么是事情谣传 <br />
<br />
　　 事情谣传是指没有科学依据的所谓将要发生事情的传言。凡“预报”的事情震级很精确，发震时间、地点很具体的传闻是谣传，凡带有封建迷信色彩和离奇古怪传说的事情传闻是谣传。事情谣言的出现原因，一方面是部分人群心理恐慌，另一方面则暴露出人们事情知识的缺乏。 <br />
<br />
　　 二、事情谣传及其传播的危害性 <br />
<br />
　　 事情谣传具有很大的危害性，一场范围较大的事情谣传，造成的经济损失有可能不亚于一个破坏性事情。事情谣传一旦发生，会借助人们对事情的无知和恐惧心理，迅速扩散蔓延，造成社会动荡，带来不应有的损失。中国是事情多发国家，但民众的防灾抗灾知识普及和科学防患意识却很薄弱，宣传普及也不够到位。防止事情谣传最根本的一条，就是坚信人民政府。坚持“不相信、不传播、及时报告”的原则。 <br />
<br />
　　 三、科学地认识事情预报及其复杂程度 <br />
<br />
　　 事情预报的基本任务是预测出将要发生事情的时间、地点和震级。时间、地点和震级称为事情预报“三要素”。一次完整的短临预报的过程是：由事情科学家对将要发生的事情三要素做出判断，上报人民政府，由省一级人民政府决定并向社会发布。 <br />
<br />
　　 对事情预测，主流科学界至今认为，迄今事情还不能准确预报，尤其是中近期事情。从世界事情史的资料分析，人类对事情的准确预报的确还是极少数。所以，大家不可轻信事情谣言，以免引发不必要的恐慌。事情局如果监测到事情前兆或临震信息，会进行科学判定并及时向社会发布。如果你一旦发现异常的自然现象（在震前的一段时间内，震区附近总会出现一些异常变化）。如地下水的变化，突然升、降或变味、发浑、发响、冒泡。气象的变化，如天气骤冷、骤热，出现大旱、大涝，电磁场的变化、临震前动物、植物的异常反应等等不要轻易作出马上要发生事情的结论，更不要惊慌失措，而应当弄清异常现象出现的时间、地点和有关情况，保护好现场，向事情部门或政府报告，让事情部门的专业人员调查核实，弄清事情真相。 <br />
<br />
　　 四、我国事情预报发布的管理制度 <br />
<br />
　　 事情预报是世界公认的科学难题，在国内外都处于探索阶段。目前，有关方法所观测到的各种可能与事情有关的现象，都呈现出极大的复杂性。<br />
<br />
　　 《中华人民共和国防震减灾法》明确规定，国家对事情预报意见实行统一发布制度。 <br />
<br />
　　 全国性的事情长期预报和事情中期预报，由国务院发布。事情短期预报和临震预报，由省、自治区、直辖市人民政府按照国务院规定的程序发布。 <br />
<br />
　　 任何单位或者从事事情工作的专业人员关于短期事情预测或者临震预测的意见，应当报国务院事情行政主管部门或者县级以上地方人民政府负责管理事情工作的部门或者机构按照前款规定处理，不得擅自向社会扩散。 <br />
<br />
　　 五、识别事情谣传的知识和方法<br />
<br />
　　1、由政府（省级）发布的事情预报。应该完全相信！因为它是事情监测预报部门的科技人员通过收集监测到的大量事情异常信息，经过认真仔细综合性的研究，参照以往的成功经验，非常慎重地提供给政府决策部门，由政府依据防震减灾有关法规，本着为人民生命财产安危，社会稳定着想而做出的。<br />
<br />
　　2、不是政府发布的，但预报事情的时间、地点、震级非常准确。不可信！事情是一种非常复杂的自然现象，虽然科学家们在事情预测作出了不懈的努力，也取得了一些进展，但目前还缺乏对事情产生的原因和发生规律的科学揭示。目前的事情预报仅处于探索阶段，不可能做得非常精确。因此，那种时间精确到某天、甚至到某时某分，地点在某个很小的范围，震级也很准确的事情预报超越了目前的科学水平。<br />
<br />
　　 3、是某某专家预报的。不可信！根据我国《事情预报发布的管理规定》，任何个人都无权擅自向社会发布事情预报意见或消息，不管他是谁。由于事情预报处于探索阶段，因而，科学家的预测意见只能提供给事情部门做参考。将个人的预测意见向社会散布本身就是违法的。 <br />
<br />
　　 4、国外科学家预报我国事情。不可信！事情预测是世界性的科学难题，我国的事情预报水平处在世界领先地位，在事情预测方面，国外水平不比我国高多少。国外科学家预测我国事情必须按照我国法律进行。除省、自治区、直辖市政府可以发布事情预报外，任何个人、组织、都无权发布，外国人也如此。 <br />
<br />
　　 5、看见或听说事情部门在做事情宣传或其他相关工作，是否要事情了？不是！让广大社会公众了解事情科普知识，提高减灾意识和能力，做好防震减灾工作，在任何时期都是事情工作部门的神圣职责。 <br />
<br />
　　 6、听亲戚朋友说要发生事情了。不可信！他们又是听谁说的？因此，一般社会上的事情谣言都不应相信，否则会造成不良社会影响，甚至破坏社会安定。听到传言后应当与当地政府和事情部门联系。 <br />
<br />
　　 7、发生有感事情过后，是否有更大的事情？是否有，应当关注政府公告。 <br />
<br />
　　 8、带有迷信色彩或别有用心的有关发生事情的消息。不仅不相信，而且要报告公安机关。</div>

							
							
							
							
