													<h2>泉州地震局长：国外预测我国地震必须按照我国法律进行</h2>
						
						
																											<div id="postmessage_780670" class="t_msgfont">[转]http://www.qzdzj.gov.cn/zhuanti/zt2008060401/1.htm<br />
　<br />
一、什么是地震谣传 <br />
<br />
　　 地震谣传是指没有科学依据的所谓将要发生地震的传言。凡“预报”的地震震级很精确，发震时间、地点很具体的传闻是谣传，凡带有封建迷信色彩和离奇古怪传说的地震传闻是谣传。地震谣言的出现原因，一方面是部分人群心理恐慌，另一方面则暴露出人们地震知识的缺乏。 <br />
<br />
　　 二、地震谣传及其传播的危害性 <br />
<br />
　　 地震谣传具有很大的危害性，一场范围较大的地震谣传，造成的经济损失有可能不亚于一个破坏性地震。地震谣传一旦发生，会借助人们对地震的无知和恐惧心理，迅速扩散蔓延，造成社会动荡，带来不应有的损失。中国是地震多发国家，但民众的防灾抗灾知识普及和科学防患意识却很薄弱，宣传普及也不够到位。防止地震谣传最根本的一条，就是坚信人民政府。坚持“不相信、不传播、及时报告”的原则。 <br />
<br />
　　 三、科学地认识地震预报及其复杂程度 <br />
<br />
　　 地震预报的基本任务是预测出将要发生地震的时间、地点和震级。时间、地点和震级称为地震预报“三要素”。一次完整的短临预报的过程是：由地震科学家对将要发生的地震三要素做出判断，上报人民政府，由省一级人民政府决定并向社会发布。 <br />
<br />
　　 对地震预测，主流科学界至今认为，迄今地震还不能准确预报，尤其是中近期地震。从世界地震史的资料分析，人类对地震的准确预报的确还是极少数。所以，大家不可轻信地震谣言，以免引发不必要的恐慌。地震局如果监测到地震前兆或临震信息，会进行科学判定并及时向社会发布。如果你一旦发现异常的自然现象（在震前的一段时间内，震区附近总会出现一些异常变化）。如地下水的变化，突然升、降或变味、发浑、发响、冒泡。气象的变化，如天气骤冷、骤热，出现大旱、大涝，电磁场的变化、临震前动物、植物的异常反应等等不要轻易作出马上要发生地震的结论，更不要惊慌失措，而应当弄清异常现象出现的时间、地点和有关情况，保护好现场，向地震部门或政府报告，让地震部门的专业人员调查核实，弄清事情真相。 <br />
<br />
　　 四、我国地震预报发布的管理制度 <br />
<br />
　　 地震预报是世界公认的科学难题，在国内外都处于探索阶段。目前，有关方法所观测到的各种可能与地震有关的现象，都呈现出极大的复杂性。<br />
<br />
　　 《中华人民共和国防震减灾法》明确规定，国家对地震预报意见实行统一发布制度。 <br />
<br />
　　 全国性的地震长期预报和地震中期预报，由国务院发布。地震短期预报和临震预报，由省、自治区、直辖市人民政府按照国务院规定的程序发布。 <br />
<br />
　　 任何单位或者从事地震工作的专业人员关于短期地震预测或者临震预测的意见，应当报国务院地震行政主管部门或者县级以上地方人民政府负责管理地震工作的部门或者机构按照前款规定处理，不得擅自向社会扩散。 <br />
<br />
　　 五、识别地震谣传的知识和方法<br />
<br />
　　1、由政府（省级）发布的地震预报。应该完全相信！因为它是地震监测预报部门的科技人员通过收集监测到的大量地震异常信息，经过认真仔细综合性的研究，参照以往的成功经验，非常慎重地提供给政府决策部门，由政府依据防震减灾有关法规，本着为人民生命财产安危，社会稳定着想而做出的。<br />
<br />
　　2、不是政府发布的，但预报地震的时间、地点、震级非常准确。不可信！地震是一种非常复杂的自然现象，虽然科学家们在地震预测作出了不懈的努力，也取得了一些进展，但目前还缺乏对地震产生的原因和发生规律的科学揭示。目前的地震预报仅处于探索阶段，不可能做得非常精确。因此，那种时间精确到某天、甚至到某时某分，地点在某个很小的范围，震级也很准确的地震预报超越了目前的科学水平。<br />
<br />
　　 3、是某某专家预报的。不可信！根据我国《地震预报发布的管理规定》，任何个人都无权擅自向社会发布地震预报意见或消息，不管他是谁。由于地震预报处于探索阶段，因而，科学家的预测意见只能提供给地震部门做参考。将个人的预测意见向社会散布本身就是违法的。 <br />
<br />
　　 4、国外科学家预报我国地震。不可信！地震预测是世界性的科学难题，我国的地震预报水平处在世界领先地位，在地震预测方面，国外水平不比我国高多少。<strong>国外科学家预测我国地震必须按照我国法律进行。除省、自治区、直辖市政府可以发布地震预报外，任何个人、组织、都无权发布，外国人也如此。 </strong><br />
<br />
　　 5、看见或听说地震部门在做地震宣传或其他相关工作，是否要地震了？不是！让广大社会公众了解地震科普知识，提高减灾意识和能力，做好防震减灾工作，在任何时期都是地震工作部门的神圣职责。 <br />
<br />
　　 6、听亲戚朋友说要发生地震了。不可信！他们又是听谁说的？因此，一般社会上的地震谣言都不应相信，否则会造成不良社会影响，甚至破坏社会安定。听到传言后应当与当地政府和地震部门联系。 <br />
<br />
　　 7、发生有感地震过后，是否有更大的地震？是否有，应当关注政府公告。 <br />
<br />
　　 8、带有迷信色彩或别有用心的有关发生地震的消息。不仅不相信，而且要报告公安机关。</div>

							
							
							
							
