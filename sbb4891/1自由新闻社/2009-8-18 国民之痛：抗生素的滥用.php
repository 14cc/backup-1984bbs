													<h2>国民之痛：抗生素的滥用</h2>
						
						
																											<div id="postmessage_207877" class="t_msgfont">国民之痛：抗生素的滥用发表于 2009-08-18 13:55:25（转自凤凰网） <br />
&nbsp;&nbsp;很想谈些许中国之医疗卫生的话题，月前，也曾谈过中医的若干事，然后，并不能扎疼人，大约是麻木惯了罢。其实中国人是很看重生死的，然而，看重归看重，对于很多危及生命的事，其实不察，或许，这正是国人是本性，只注重切身之利，或是切身之害罢。然而，威胁虽未立至，其暗影却在渐渐笼罩过了，于是，我见，我闻，我说。<br />
<br />
细菌感染曾是人类第一死因，在经历了漫长的黑夜之后，抗生素的发明带给了人类带来了光明的火种。1928年的一次医学实验中，一个葡萄球菌培养基受到了一种霉的污染。试验者弗莱明注意到，霉周围的细菌消失了。他断定这种霉生产了某种抑制葡萄球菌生长的物质，并将这种物质命名为青霉素。在他的启发下，英国医学家弗洛里和钱恩提纯了青霉素，先后给实验室动物和病人试用，证明青霉素对细菌感染有惊人疗效。三人因此获得了1945年的诺贝尔生理学或医学奖。青霉素是人类历史上第一种抗生素，在第二次世界大战中挽救了无数战争伤员，揭开了抗生素家族救苦救难的开端，人类平均寿命因此增加10岁。医学史上，很多细菌感染曾是无药可治的绝症，造成了难以计数的人类死亡。到1980年代，随着品种众多、价格低廉的抗生素广泛随意地使用在医学和其他领域，医学界人士乐观地认为，抗生素已经彻底解决了细菌感染的问题。从人类历史上第一种抗生素――青霉素，发明到今日，不过70多年的时间，然而这希望的火种正日益暗淡着。并非危言耸听，如果不解决抗生素滥用的问题，等待人类的将是下一次黑暗。<br />
<br />
什么是抗生素滥用？凡是超时、超量、不对症使用或未严格规范使用抗生素，都属于抗生素滥用。抗生素的普遍使用使越来越多的细菌产生了耐药性，医生们发现，原来有特效的抗生素对耐药菌的作用越来越小。每一种抗生素投入使用，没有被杀灭的细菌会迅速产生对这一抗生素的抗体，成为耐药菌。如绿脓杆菌可以改变细胞膜的通透性，阻止青霉素类药物的进入；结核杆菌通过改变体内蛋白质结构阻止抗生素与其结合；更有甚者，有的革兰氏阴性菌可以主动出击，用水解酶水解掉青霉素和头孢菌素类药物。这种耐药性既能横向被其他细菌所获得，也能纵向遗传给后代。<br />
<br />
从细菌的耐药发展史可以看出，在某种新的抗生素出现以后，就有一批耐药菌株出现。医学工作者开发一种新的抗生素一般需要10年左右的时间，而一代耐药菌的产生只要2年的时间，抗生素的研制速度远远赶不上耐药菌的繁殖速度。于是医生一边加大用药剂量，研究者一边研究新药。更先进的抗生素不断发明，细菌的耐药性同时飞快增强，一场抗生素和细菌耐药性的赛跑始终在进行着。<br />
<br />
这场赛跑的胜者是谁，现在难以预料，但现在的情况并不乐观。在被称为抗生素“黄金时代”的五六十年代，全世界每年死于感染性疾病的人数约为700万，而这一数字到了1999年上升到2000万。美国1982至1992年间死于传染性疾病的人数上升了40%，死于败血症的人数上升了89%。造成病死率升高的主要原因是耐药菌带来的用药困难。临床上很多这样的现象：由于耐药菌引起感染，抗生素无法控制，最终导致病人死亡。<br />
<br />
耐药菌的发展速度令人触目惊心。20年代，医院感染的主要病原菌是链球菌。而到了90年代，产生了耐甲氧西林的金黄色葡萄球菌(MRSA)、肠球菌，耐青霉素的肺炎链球菌、真菌等多种耐药菌。大量耐药菌的产生，使难治性感染越来越多，导致病菌感染的机会越来越多，治疗感染性疾病的费用越来越高。如耐青霉素的肺炎链球菌，过去对青霉素、红霉素、磺胺等药品都很敏感，现在几乎“刀枪不入”。绿脓杆菌对氨苄西林、阿莫西林、西力欣等8种抗生素的耐药性达100%，肺炎克雷伯氏菌对西力欣、复达欣等16种高档抗生素的耐药性高达52%-100%。<br />
<br />
目前从全世界的范围内，政府都在严禁抗生素的滥用。因为滥用带来的可怕后果是：有朝一日病菌耐药性“进化”速度超过抗生素的研制速度，我们又将回到抗生素诞生前的黑暗岁月。<br />
<br />
医学界流行一句话说，在美国买枪很容易，但买抗生素却很难。而中国正好相反。从长远看，抗生素滥用的危害比枪支的危害大。世界军医会议召开期间，美国陆军卫生部长对我国的一位博士说，美国对抗菌药物控制很严格，定期考核医生的抗菌药知识，不及格者将停止其处方权。听说第三代头孢在中国谁都可以买，到处可以开，是这样吗？在美国这是不可能的。<br />
<br />
事实确实如此。目前，中国的门诊感冒患者约有75%应用抗生素，外科手术则高达95%。世界卫生组织调查显示，中国住院患者抗生素药物使用率高达80%，其中使用广谱抗生素和联合使用两种以上抗生素的占58%，远远高于30%的国际水平。<br />
<br />
复旦大学附属儿科医院的统计数据显示，该院每年销售收入排序前3位的药物均为抗生素。2001年，15种最畅销的药物中抗生素药品就占了11种。此外，静脉注射已经成了滥用抗生素的新途径。每天1000个呼吸道感染的门诊患者里，有将近2／3会接受静脉注射治疗。儿科医院静脉应用抗生素呈现出逐年增多的趋势：1996年注射用抗生素消耗金额占全部抗生素消耗金额的比例为46.7%，而2001年上升到53.6%。<br />
<br />
据统计，仅超前使用第三代头孢菌素，全国一年就多花费7亿多元。滥用抗生素的直接恶果就是耐药性的日渐严重。以中国细菌耐药性最为严重的地区之一上海为例，上海人群感染的金黄色葡萄球菌中，80%已经产生了对青霉素G的耐药性。一些药品的有效率已经跌到了20%。在一些地方，预防结核病的卡介苗的有效率只有30%。<br />
<br />
以对甲氧西林耐药的葡萄球菌(MRSA)为例，80年代初，95%有效，没过5年，1985年，有效率一下子下降了20%，到1996年，72%无效，现在基本没用。20年前抗菌药环丙沙星开始在临床上应用，当时被证明是副作用小、治疗效果好的药品，而现在环丙沙星几乎对60%以上的病人失去了作用。某些时候，除万古霉素外，这些葡萄球菌已经无药可治。但是在1992年，美国首次发现了可对万古霉素产生耐药性的MRSA，医生几乎无药可用。<br />
<br />
抗生素专家张永信教授说，“我越来越心虚，因为我们武器库里的武器已经越来越少。”他忧心忡忡：因为旷日持久的滥用，曾经是治疗细菌感染“灵丹妙药”的抗生素，越来越多地失效，这意味也许不久的将来，罹患细菌感染的中国人，将面临无药可用的境地。<br />
<br />
目前，在全国各大中小城市以及乡村，密布着各等规模的“药店”、“药房”、“大药房”，和小食杂店一样的多，到处都能看得到。药房里面出售的通常有“保健品”、“保健食品”、“保健药品”等等，这些都是非处方用药。这些药品在药店里出售极大的方便了人民群众的生活。而抗生素属于处方用药，它的出售应该是受到很大的限制的，不是说谁都能到药店里买得到的，国家对于这类药品是有着严格的要求的，没有医生的处方，药店是绝对不可以卖给顾客的。<br />
<br />
但是，事实上不尽如此，虽然任何一家药店或者大药房都非常醒目的写着“RP”凭医生处方购买等字样，但这只是一纸空文。实际上，不用凭医生的处方,任何人到任何一家药店都能够买到抗生素（包括任何针剂）等处方用药。甚至，有很多的药店或者大药房还配有护士，就在药房里面直接的为买药者进行静脉注射。这些人根本不懂得药物的性能原理及使用适应症；禁忌症；使用的疗程等；不懂得发生输液反映或者过敏反应的处理与抢救，使得在药房里打针致死人命案时有发生。<br />
<br />
有一组数字，或许能说明问题：每年有20万中国人死于药物不良反应，其中，因抗生素滥用造成的死亡要占到40%。中国三分之一的残疾人属于听力残疾，而60%到80%的致聋原因与使用抗生素有关。<br />
<br />
不管怎么样，打针致死致残的毕竟是少数，更多数的情况是患者在不知不觉中产生了可怕的抗药性。感冒了到药店买点抗生素或者液体，用电话叫来一位护士，花上几元钱手工费，就在药房或者居民的家中进行输液、治病。病情刚刚好了一点就停药了，这样久而久之，在人的体内就逐渐的产生了耐药性！全民都这样来当医生，用不了几年，用过药的这些人就是耐药一族了！我国研制生产一种新的抗菌素，得需要10年的时间，而人类若是乱用抗菌素只需要1-2年就可以产生耐药性。<br />
<br />
　由于我们国家现在的法律不健全；国民对抗生素的使用也没有正确的认识，再加上执法队伍监管不力，有关的法律滞后，最终放纵了抗生素使用的泛滥。然而抗生素的滥用，决不仅仅是一个个人的力量就能解决的问题。比如有人说：自己和家人都尽量避免使用抗生素，因此耐药菌不会在自己身上形成。然而，事实远比这种一厢情愿的想法残酷。时至今日，中国人几乎生活在一个无处不有抗生素、因此无处不有耐药菌的环境中。“洁身自好”并不能保证自己不成为抗生素滥用的受害者。<br />
<br />
根据2001年的数据，世界抗生素市场的平均年增长率为8%左右，而1998年全球抗感染药物市场总销售额达400亿美元，抗生素达到260亿美元。美国是世界上最大的抗生素市场，抗生素市场的年销售额达100亿美元；日本则占世界抗生素市场的35%，其中头孢类占日本市场的34%。在中国医药市场中，抗感染药物已经连续多年位居销售额第一位，年销售额为200多亿元人民币，占全国药品销售额的30%，全国数千家国家药品生产企业中，有1000多家生产各类抗生素，市场竞争异常激烈。<br />
<br />
如此之多的抗生素被生产出来，并不都是用在了人类身上。动物都与人一起使用起抗生素，因为使用抗生素可以使它们生长更快，生产产品更多，获利更多。近年来，发达国家意识到在动物饲料中添加抗生素的危害性，从1996年开始，立法禁止在动物饲料中使用抗生素。但欧盟的科学家披露，即使如此，美国生产的抗生素还是约有70%用来饲养动物。我国每年生产的700吨喹诺酮，仅这一种抗生素就有一半用于养殖业。这导致中国地区大肠杆菌对喹诺酮的耐药性已达到60%。<br />
<br />
在农业中使用的抗生素种类已经囊括了人类自身使用的全部抗生素的种类。饲养动物使用抗生素，可以导致耐药菌株的出现。这些耐药菌又通过食物或动物与人的接触，传播给了人。美国伊利诺伊大学的科学家发现，土壤和农田地下水中的细菌从来自猪的肠道菌那里获得了耐受四环素的耐药基因。耐药基因长期存在于土壤和水生细菌中，而且可能传播到那些毒性很强的细菌身上。如果人饮用这样的水，毒性很强的耐药细菌也会传播给人。而人类消费排泄的废物污染环境和食物链，自然环境中的细菌接触到各种各样的大量抗生素，由此产生了耐药性，导致抗生素的耐药基得以蔓延：细菌耐药性扩散的链条由此形成。<br />
<br />
我们吃的粮食、蔬菜、肉类、乳品乃至医院的空气中，都因为抗生素的滥用，充满了抗生素和耐药菌。这些耐药菌通过饮食、呼吸进入了我们的身体。人类本身成了一个耐药菌库。一旦发生细菌感染，即使从来没有使用过抗生素药物的人，同样可能面临抗生素治疗束手无策的局面。<br />
<br />
哎，如此之局面，真是触目惊心啊。中国的问题并不止是腐败这一问题，些许漫不经心的事，正是它，要了你的命，还不知晓。卫生部这么多年来，对于抗生素的滥用，有着不可推卸的责任，呜呼！这是拿人民的健康开玩笑。<br />
