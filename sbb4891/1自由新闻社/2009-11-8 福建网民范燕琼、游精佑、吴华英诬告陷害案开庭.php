													<h2>福建网民诬告陷害案庭审纪实 / 刘晓原</h2>
						
						
																											<div id="postmessage_323871" class="t_msgfont"><a href="http://lawyerxiaoyuan.blog.sohu.com/136531447.html" target="_blank">http://lawyerxiaoyuan.blog.sohu.com/136531447.html</a><br />
<br />
2009-11-12 <br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;题图为当天在法院门口的林秀英（林朝阳摄）<br />
<br />
&nbsp; &nbsp;11月11日上午7时，我与李方平律师、金光鸿律师，乘车前往马尾区。在马尾区吃了早点，到达法院已是8时10分。<br />
<br />
&nbsp; &nbsp; 马尾法院门外，已经有七八十人在围观。在法院大门外，有人在排队进入法院。我看到有一个人拿着十多张旁听证，分发给一同前去旁听的人，看来他是一个领队。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;八时十五分，我们凭开庭传票和律师证进入法院院内。在经过法庭外的安检门时，法警要检查我们的公文包，我当即表示不同意。按照最高人民法院的规定，律师进入法院开庭，如出示了律师执业证，就可以不用进行安检，这是最高人民法院给律师的一点特权。<br />
<br />
&nbsp; &nbsp; 法警让我们在安检门边等着，不让我们进入法庭，说要请示领导。<br />
<br />
&nbsp; &nbsp; 这时，我看见有人在安检门旁的窗口办理旁听登记，我注意看了一下，有人拿着警官证办理登记。原来，法院的很多旁听证，发给了警方。难怪记者申请不到旁听证。<br />
<br />
&nbsp; &nbsp;看到公诉人早已在法庭上坐好，我请法警让我们进入法庭，以便作好庭审准备工作。法警说还得再等一下。随后，有一个高个子法警出来了，说可以不用安检，但要打开公文包看一下。我说，打开包来看，这不是检查吗？高个子法警反复强调，打开看一下，不属于检查。<br />
<br />
&nbsp; &nbsp; 在我的坚持下，法警要我们拿出律师进入法庭可以不安检的规定。我说，应该是你们拿出律师要安检的规定才对。双方争则起来，一女法警劝解我们不要生气。快到开庭时间了，女法警叫我们进入法庭，还提醒我们不要录音，要尊重法庭的秩序。我说，一定会遵守法院的规定，但法院也要遵守法律的规定。<br />
<br />
&nbsp; &nbsp; 我们进入法庭，刚放好案卷材料，就开始宣布开庭。时间是八时四十分，比原定时间晚了十分钟。<br />
<br />
&nbsp; &nbsp; 法官宣布开庭后，法警将被告人带进了法庭。范燕琼坐着轮椅由法警推着进入法庭，她见到女儿在旁听席上，情绪变得激动起来，喊着要与女儿拥抱，还大声地哭泣起来。<br />
<br />
&nbsp; &nbsp; 吴华英看见坐在旁听席的女儿与母亲时，情绪也很激动。吴华英反复地质问法官，你们能不能坚持独立审判？<br />
<br />
&nbsp; &nbsp;游精佑看到旁听席上的妻子与女儿，还是控制住了自己情绪。<br />
<br />
&nbsp; &nbsp;因范燕琼、吴华英情绪激动，审判长只好宣布休庭五分钟，将三个被告人带到休息室。<br />
<br />
要辩护律师去做工作，以稳定她俩的情绪。<br />
<br />
&nbsp; &nbsp; 九时许，审判长宣布复庭。<br />
<br />
&nbsp; &nbsp; 审判长询问了被告人范燕琼、游精佑、吴华英基本情况，以及是否收到起诉书等问题。<br />
<br />
&nbsp; &nbsp; 当审判长告知被告人的权利时，吴华英情绪激动，还在质问法官，能不能行使独立审判权？真是难为人家法官了。官权指示的案件，叫法官如何行使独立审判权呀？<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;我们见到吴华英和游精佑手上戴着手铐，就向法庭提出了解除刑具要求，审判长接受律师意见，叫法警打开了手铐。<br />
<br />
&nbsp; &nbsp;9时14分，由公诉人宣读起诉书。<br />
<br />
&nbsp; &nbsp;9时22分，当公诉人念到被告人范燕琼捏造他人犯罪事实时，范燕琼情绪马上失控，大声指责公诉人没有实事求是，起诉书完全是在瞎指控。<br />
<br />
&nbsp; &nbsp;9时25分情绪有所稳定后，范燕琼说早上没有吃药，法警递上矿泉水让她吃药。范燕琼在被抓获之前，身体并不太好。刚关进看守所时，还绝食过几天，律师第一次会见她，就已经无法走路。律师向看守所提出她的身体状况问题后，看守所也引起了重视将她送入医院治疗。<br />
<br />
&nbsp; &nbsp;9时31分，当公诉人念到起诉书中“被告人手段卑鄙”时，范燕琼情绪失控，大发脾气，说自己同情林秀英才帮助她写文章，以其引起政府的重视，根本没有其他目的。也没有收过林秀英一分钱。吴华英也很激动，指责公诉人是在侮辱人格。游精佑对这个指控，同样表示反感，认为没有事实依据。<br />
<br />
&nbsp; &nbsp;范燕琼越讲越激动，不时还带有骂声，场面开始失控，9时34分许，审判长以违反庭审纪律为由，叫法警将范燕琼带出法庭。吴华英也在不停指责公诉人，审判长又叫法警把她带出，见范与吴被带出法庭，游精佑表示要退庭，审判长称，你要下去的话，将继续羁押。<br />
<br />
&nbsp; &nbsp;范燕琼情绪失控被带出法庭，我们律师提出了抗议。<br />
<br />
&nbsp; &nbsp;说游精佑还要继续羁押，对审判长之言，我提出了异议。案件还没有判决，凭什么下结论还将继续羁押？难道还没有审判，法官就知道了案件结果。<br />
<br />
&nbsp; &nbsp; 三个被告人全部退庭后，我们表示没有被告人，无法继续审判下去。审判长说，他们的情绪失控，违反了庭审纪律，不带下去无法审判。我说，他们情绪一时变得失控，带下去后冷静下来了，就应当把他们带上来继续听审。审判长接受了这个意见，叫法警把被告人带入法庭继续听审。<br />
<br />
&nbsp; &nbsp; 范燕琼在法庭上陈述，所写文章内容是林秀英所说，自己并没有捏造事实。写文章的目的是出于同情林秀英。<br />
<br />
&nbsp; &nbsp;游精佑、吴华英表示，视频内容全部是林秀英与她的哥哥在讲述，没有为她编造什么虚假事东西。制作视频的目的，同样是出于同情，将视频发到网络，是为了引起政府重视，尽快处理严晓玲之死的问题。<br />
<br />
&nbsp; &nbsp; 公诉人举证时，将十六大本厚厚的案卷分四个部份举证。在举第一部份证据时，公诉人不宣读对被告人有利的证词，辩护律师提出了抗议。并对公诉人捆绑举证方式表示异议，由于控辩双方争议很大，被告人也表示不满，审判长宣布休庭，召集公诉人与辩护律师交换意见。<br />
<br />
&nbsp; &nbsp; 复庭后，公诉人还是以捆绑方法举证。抗议也起不了作用。<br />
<br />
&nbsp; &nbsp; 对公诉人所举的证据，我的观点是，恰恰证明了范燕琼、游精佑、吴华英没有捏造他人的犯罪事实。这起诬告陷害案，如果存在诬告陷害他人犯罪事实的话，也应是林秀英在捏造。林秀英在公安讯问笔录中均已承认，范燕琼文章所写的内容是她自己所说。<br />
<br />
&nbsp; &nbsp; 在公诉人看来，公安机关召开了新闻发布会，民众就一定要相信，就不可继续质疑。这个观点太可笑了。我拿广西法官黎阳之死和云南躲猫猫之事加以驳斥。<br />
<br />
&nbsp; &nbsp; 公诉人还有一个潜意识观点，我们公安人员是遵纪守法的，不可能参与卖淫、贩卖毒品之事，不可能与人合伙办歌厅。对此观点，我用重庆打黑予以驳斥。<br />
<br />
&nbsp; &nbsp; 11月5日，律师曾向法院申请要求林秀英和林爱德等人出庭，同时通知本案的五十七个证人出庭。范燕琼与吴华英在法庭上，也几次向法庭要求叫林秀英来对质。但没有得到法庭的允许。<br />
<br />
&nbsp; &nbsp; 林秀英在法院门外等了一天，希望能进入法庭参与庭审。但一直等到晚上近八时，我们离开法院，她还在法院门外。林秀英曾经多次说过，他们是为了帮助她被抓。他们判几年，严晓玲的尸体就放几年。林秀英这个不幸的女儿，还是心存良知，还能分辨出好坏。<br />
<br />
&nbsp; &nbsp; 在庭审中，范燕琼因身体有病，中途吃了药，医师给她吸了氧气，量了血压。经过这起案件的折腾，我感觉范燕琼精神方面也似乎有问题了。<br />
<br />
&nbsp; &nbsp; 庭审现场，有一台大摄像机，还有两台小摄像机，其中有一台小的摄像机，是法院工作人员在使用。据说，当地媒体也没能进入旁听。<br />
<br />
&nbsp;&nbsp;中午休庭时，法院为我们律师和旁听人员提供了免费午餐。<br />
<br />
&nbsp; &nbsp;晚上七时四十分，庭审终于结束，没有当庭作出宣判。<br />
<br />
&nbsp; &nbsp; 我们六位律师，为范燕琼、游精佑、吴华英做的是无罪辩护。<br />
<br />
&nbsp; &nbsp;范燕琼说，但愿自己是最后一个因言获罪的人。愿望很好，但在权大于法年代，你怎么可能会是最后一个呢？想做最后一个“囚徒”，奢望也！<br />
<br />
&nbsp; &nbsp; 走出法院，发现天在下着大雨。离开法院有一段路了，林洪楠律师接到审判长电话，说庭审笔录要被告人签字，希望律师也到场见证一下。我们只好下车再回法院。等被告人看了笔录签完字离开法院，已是晚上九时了，打不到出租车，只好乘坐公交回到福州市区，吃完晚饭回到酒店十时多了。刑事案件的庭审笔录，辩护律师不用签字，全国各地都是同一规定。不知，这样的规定是不是出自最高法院规定？反正在《中华人民共和国刑事诉讼法》里找不到。<br />
<br />
&nbsp; &nbsp; 在结束本文时，我还要说一下，法官和法警的表现。以我的观察，他们也是同情三个被告人，对范燕琼态度很好。<br />
<br />
&nbsp; &nbsp; 面对范燕琼和吴华英驳斥，公诉人还是很克制。非常遗憾的是，当林洪楠律师指出，在审查起诉阶段与公诉人交换意见时，公诉人称吴华英在QQ聊天里，对政府有不满的态度。听了林洪楠律师话，其中的一位女公诉人，在随后发表意见时，情绪竟然也失控了，说没有讲过这样的话，指责林洪楠是一个七十岁老律师讲话要有证据。想不到的是，女公诉人从口中又冒出一句话， “你是胡说八道”。<br />
<br />
&nbsp; &nbsp; 对公诉人的不文明用语，我向审判长提出了意见，要求制止公诉人的无理。如果林洪楠律师所说没有依据，作为公诉人也不能骂人。两个公诉人在法庭上是行使职务行为，要治这三个网民的诬告陷害罪，也不是他们所能左右的。在法庭上受气，实在也有点冤，但公诉人受了被告人的气，是不能发到律师身上。这个公诉人，还很年轻，在此我不点她的名，希望在以后的庭审中，一定要接受这个教训，要注意文明用语，以维护检察机关的公平、公正、文明形象。当然啦，我也要检讨自己，个子不高，声音很高，可我没有使用不文明语言。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;这起网民诬告陷害案的庭审，是最好的一堂法制教育课，相信在现场旁听的年轻警员，会从这起案件悟出民众的艰辛，以后会更加坚持“立警为民”理念。<br />
<br />
&nbsp; &nbsp; 庭审情况太复杂了，很多细节性内容留待以后慢慢写了。<br />
<br />
&nbsp; &nbsp; 这起案件中，有人虽然“幸运”逃脱了刑事指控，但有关部门仍然不放过他们，已经责令所在单位进行政纪处罚。而“治理”范燕琼、游精佑、吴华英，很明显不是法律，而是官员的指示。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;这起案件，存在着几个公安机关取证问题。这严重违反《中华人民共和国刑事诉讼法》和公安部《公安机关办理刑事案件程序规定》。公诉人称，这是由专案组办案，可以由不同机关取证。我问公诉人，是哪一级的专案组，是马尾区，还是福州市，抑或是福建省？公诉人不出声了。<br />
<br />
&nbsp; &nbsp; 这不是重大的暴力犯罪案，这只是一起由两篇文章、一个视频引发的诬告陷害案，有必要动用什么专案组吗？这不是杀鸡焉用牛刀了？<br />
<br />
&nbsp; &nbsp; 最后，我用九个字，总结福建网民诬告陷害案：太荒唐（为别人写文章、制作视频，内容不是作者捏造，没有诬告陷害意图，竟然要作者承担刑事责任）、太离谱（没有一份证据能证明，文章作者和视频制作人，捏造了他人犯罪事实，“主谋”不审，只审“帮凶”）、太可怕（官员的一个指示，可以兴师动众抓你。敢帮助访民维权，曝光地方丑闻，就叫你进监狱，你不会感到可怕吗？）</div>

							
							
							
							
