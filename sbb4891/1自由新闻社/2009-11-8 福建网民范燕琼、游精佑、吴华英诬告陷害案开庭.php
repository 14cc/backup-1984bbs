福建网民范燕琼、游精佑、吴华英诬告陷害一案，本月十一日上午八时三十分在福州市马尾区人民法院开庭审理。<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;11月5日，是林洪楠律师七十岁生日。因福建网民诬告陷害案即将开庭，他也没有休假过生日，而是与姜律师、吴律师一起去了福州市第二看守所会见范燕琼、游精佑、吴华英。<br />
<br />
&nbsp; &nbsp; 林洪楠律师是福建省法炜律师事务所主任，在福建网民诬告陷害案中，他担任被告人吴华英的辩护律师。<br />
<br />
&nbsp; &nbsp; 林洪楠律师在看守所会见时，福州市司法局律师管理处领导跑来看守所，给他送来了一份《行政处罚当事人听证告知书》。林洪楠笑称，这是一份最好的生日礼物。<br />
<br />
&nbsp; &nbsp; 告知书中称，林洪楠律师在执业活动中，将属国家秘密文件复印件提供给了当事人家属，造成该份案件被发布到境外网站的泄密行为，因此，根据《律师法》等相关规定，拟给予停止执业一年的行政处罚。<br />
<br />
&nbsp; &nbsp;这份惹祸的文件，涉及到吴华英弟弟的爆炸案。林洪楠律师说，这起案件前后有十多个律师介入，早在五年前，公安机关就对泄密一事进行过调查，自己还被公安机关传唤，但没有证据证明是自己所为。想不到的是，五年过去了，因为担任吴华英辩护人，有关部门又拿泄密来说事。<br />
<br />
