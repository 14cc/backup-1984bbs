													<h2>互联网救中国新论</h2>
						
						
																											<div id="postmessage_20087" class="t_msgfont">多元的民意-《金融时报》中文网专栏评O8县长<br />
<br />
多元的民意<br />
<br />
英国《金融时报》中文网专栏作家加藤嘉一 2009-01-12<br />
<br />
一位研究苏联的学者曾经说过，如果八十年代就有了互联网的话，那苏联可能不会解体。目前正在中国发生的事情似乎可以证明这一说法。<br />
<br />
2008年12月10日，匿名人士仿照捷克《七七宪章》起草的，呼吁推广“普世价值”、进行政治体制改革的《O8县长》在网上发表了。到今天为止，《O8县长》在中国和其它国家获得了包括大量知识分子在内的7000多人的网上签名支持。对于至今仍然没有公布自己身份的《O8县长》起草者来说，这个数字肯定有点遗憾。<br />
<br />
举一个有趣的例子，三年前另一个匿名者发起的反对日本“入常”的运动，以同样的方式在国内获得了60000000个签名，几乎是这次的数字的1万倍。我不知道这两个匿名起草者是不是同一个人，而他恰恰显示中国人除了万分之一以外，对民主这样的“普世价值”都不积极，除了“反对日本入常”外，对政府没有别的诉求。如果邻国真的如此，日本人不免要悲观了吧。<br />
<br />
但是，日本的未来其实没有那么糟糕。仔细读过以后，我发现《O8县长》并不像“反日”的公开信一样现实。《O8县长》对当前的社会问题说的并不多，而把大部分的篇幅放在要求当局采纳民众争取的十九条主张，而这十九条的内容才是正题。十九条主张要求北京学习八十年代末的苏联，保障人权和言论自由，减税，实行联邦制，解散武装部队中的党组织，实现军队国家化。<br />
<br />
对于当权者来说，每一条都是自杀性的，实行了的话肯定会造成现在体制的崩溃。而体制崩溃以后中国怎么办？只有几千字的《O8县长》并没有回答这个核心问题。“ 把十九条主张全部实现”本身就是《O8县长》的目的，签署者必须相信这些措施一定能消除中国面临的任何风险，并为这个信念付出对抗政府的代价。可见，与不要求国民付出任何代价的反日宣言相比，《O8县长》未免太不现实了，而且，不签署《O8县长》并不代表不支持民主。<br />
<br />
其实，即使不看原文，在经历过了08年的一系列风波之后，《O8县长》的前途本来就是不乐观的。在3月份，中国边疆的一个地区发生了12年来最严重的民族冲突，在全国面前暴露出民族政策的问题；而在之后两个多月的时间里，北京奥运会火炬在全球传递，在每一个发达国家都遇到了相当强烈的抗议，西方和全世界的媒体也站在批评中国的立场上，这一事实也是中国人普遍看到的。<br />
<br />
按说这些事全是不利于政府的“坏事”，与全世界联系这么紧密的中国人，看到这些事以后应该会思考：为什么全都反对我们？我们做错了什么事情吗？这正是中国外交部发言人经常“奉劝”美国人思考的问题。<br />
<br />
然而，中国的民众根本没有这样想过，而是非常团结的站在政府一方，相信一切都是西方的阴谋，“要求”政府对达赖、萨科奇等人采取强硬措施，甚至采取抵制家乐福的方式来显示决心。在这个过程中，接触西方生活方式和观念最多的80后年轻人自觉的接受了政府的教育，成为最有力反对西方的群体，令整个世界感到惊讶。而在08年底，金融危机使中国的经济陷入衰退，政府也同样成功的让大部分民众相信“这是美国人造成的，我国没有错”，同样把“坏”变成了“好”。在能把这么大的“坏事”变成“好事”的强大对手面前，谁也不可能用一篇几千字的文章创造奇迹<br />
而“宪章派”的国际支持者，同样没有起到有效而现实的作用。据我所知，中国政府把自己在舆论上的主要敌人称为“五毒”，包括“（疆、藏、台）独”、“轮 ”、“（民）运”，分别对待。表面上，政府的态度是宣传“五毒合流”，进行统一的批判；而实际上则想办法鼓励所有的敌人之间互相攻击。对于“宪章派”来说，吸收谁不吸收谁，是很难决定的。一方面，为了避免上述任何一个派别的反对，《O8县长》不得不加入“联邦制”的内容，造成所有的国内签署者都“可能”违反了法律；而另一方面，上述派别的公开签名加入，使得“宪章派”进一步被孤立起来。结果，既造成《O8县长》的签署状况陷入混乱而无序，也造成《O8县长》本身失去实实在在的公信力，只好接近“乌托邦”的境地。<br />
<br />
与上述的遭遇相比，更加值得思考的是《O8县长》无法落到实处的深层原因。过去，中国传统的“灌输式宣传”是比较愚蠢的，政府彻底控制了所有的宣传渠道，推行政策时，把观点完全一样的内容强行输入国民的心中。这种方式造成国民的逆反心理，碰到改革开放后涌入的大量信息后更是出了严重问题，直接导致了二十年前的那场危机。而在网络改变中国人的生活方式以后，无论是“宪章派”也好，中国的当局也好，西方的媒体也好，大家都在互联网上展开propaganda竞争，争取尽可能广泛而可靠的支持者。这种竞争是在比较开放和自由、充满辩论和争吵的环境下进行的，要想有效地在中文网络上建立起影响力，就必须考虑民众的价值观和感受，掌握说服的技巧。<br />
<br />
现实是，中国官方和其下属的宣传机构在积累了长期的经验后变得渐渐成熟，特别是在互联网出现以后，政府的宣传和说服更加巧妙了。在早期，无论是国内还是国外，都有许多人认为网络会动摇当局的执政合理性，但后来，随着策略的变化和民族主义的几次高潮，中文网络上早已形成了志愿支持政府的强大用户群体。政府宣传部门需要做的事情就是运用手里的各种资源，“发动群众斗群众”，帮助这些“网络志愿者”们尽量说服和“启发”有各种各样观点的反对者，让反对者自主的“ 觉悟”到当局的合理性。对于从西方来的各种意识、思想，可以有选择性的引入，给国民打“预防针”。有了这些志愿者和“预防针”的帮助，当局就可以用相对低的成本控制住国内的舆论压力，非常“自主”的推行内外政策，避免悲剧的重演。&nbsp;&nbsp;<br />
<br />
而与日益成熟、进步极大的对手相比，“宪章派”二十年来却没什么变化和进步，现在说的仍然是和当年在广场上完全一样的言语。就像我的一位朋友说的，《O8县长》表达的愿望虽然是建立西方式的政府，却不像西方人一样谨慎的说话，而是像“中学里的政治教师”一样，“唠叨个不停”，好像听众什么也不知道一样。其实，中国的执政者虽然没有选举做基础，但其表现至少得到了民众的默许，这就是“灌输”的资本；而“宪章派”没有资本却要“灌输”，更不可能被接受了。</div>

							
							
							
							
