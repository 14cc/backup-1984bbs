													<h2>福建规定教师在课堂散布&quot;错误言论&quot;将被解聘</h2>
						
						
																											<div id="postmessage_611326" class="t_msgfont">核心提示：福建省委教育工委、省教育厅今天出台《关于进一步加强和改进福建省高等学校教师职业道德建设的意见》，规定在教育教学工作中散布违反党的路线方针政策、党的基本理论、国家法律法规等错误言论，对学生确立正确理想信念和政治信仰造成不良影响的教师将被解聘。<br />
<br />
中国网5月12日报道 今后，福建省高校教师如果出现科研工作中弄虚作假、抄袭剽窃侵占他人成果，或者以教谋私等行为的，将实行师德“一票否决”，严重违规者将被解聘。福建省委教育工委、省教育厅今天出台《关于进一步加强和改进福建省高等学校教师职业道德建设的意见》，首次明确规定师德“一票否决”的十项具体内容，强化对高校教师的师德考核管理。<br />
<br />
意见提出了高校教师应该履行的“爱国守法，依法执教”，“爱岗敬业，恪尽责守”，“关爱学生，教书育人”，“为人师表，淡薄名利”，“严谨笃学，终身学习”等五个方面的职责，首次提出了福建省高校教师职业道德规范。同时，还进一步明确以下十种师德“一票否决”的行为：一是因违法违纪行为受到纪律处分的；二是在教育教学工作中散布违反党的路线方针政策、党的基本理论、国家法律法规等错误言论，对学生确立正确理想信念和政治信仰造成不良影响的；三是以非法方式表达诉求，故意不完成教育教学任务、扰乱正常教育教学秩序，损害学生利益的；四是组织、诱导学生参加非法组织、“黄、赌、毒”和迷信活动的；五是无故旷教或无故拒不接受学校分配的教学、科研及其他工作，影响恶劣的；六是教学工作敷衍塞责，违反教学纪律，屡教不改，造成严重教学事故、实验事故和社会实践事故的；七是在科研工作中弄虚作假、抄袭剽窃，侵占他人成果的；八是对学生安全工作不负责任，或侮辱、体罚学生，影响恶劣的；九是以教谋私，以考试分数等谋财、谋物、谋色，影响恶劣的；十是在招生、考试、评估考核、职称评聘中弄虚作假、营私舞弊的，以及其他严重违反教师职业道德的行为。<br />
<br />
福建省委教育工委、省教育厅要求各高等学校要把师德建设摆在高校教师队伍建设的首位，把师德表现作为教师年度考核的重要内容，坚持师德条件与学术条件等并重的原则，全面考核评价教师。师德评价结果将作为教师职务评聘、工资晋升、进修深造、工作调整、出国出访等重要依据。对存在师德问题的教师，将及时诫勉、警示和处分；对有严重失德行为、造成严重后果，影响恶劣的，将依据有关规定给予撤销教师资格或予以解聘。<br />
<br />
福建省委教育工委、省教育厅还要求高校要严肃处理学术不端行为，建立处理学术不端行为的工作机构，严肃处理学术不端，对学术不端行为人给予警告直至开除等行政处分；触犯国家法律的，移送司法机关处理；对于其所从事的学术工作，采取暂停、终止科研项目并追缴已拨付的项目经费，取消其获得的学术奖励和学术荣誉，以及在一定期限内取消其申请科研项目和学术奖励资格等处理措施。<br />
<br />
据了解，福建各设区市教育部门和各高等学校还将面对社会公开师德举报电话、设立师德举报箱或其他举报和反馈渠道，聘请师德师风监督员，接受学生、家长和社会对师德建设进行监督和评议，以期预防和减少违反教师职业道德行为。<br />
<br />
