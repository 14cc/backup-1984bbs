<br />
四是组织、诱导学生参加非法组织、「黄、赌、毒」和迷信活动。<br />
<br />
五是无故旷教或无故拒不接受学校分配的教学、科研及其他工作，影响恶劣。<br />
<br />
六是教学工作敷衍塞责，违反教学纪律，屡教不改，造成严重教学事故、实验事故和社会实践事故。<br />
<br />
七是在科研工作中弄虚作假、抄袭剽窃，侵占他人成果。<br />
<br />
八是对学生安全工作不负责任，或侮辱、体罚学生，影响恶劣。<br />
<br />
九是以教谋私，以考试分数等谋财、谋物、谋色，影响恶劣。<br />
<br />
十是在招生、考试、评估考核、职称评聘中弄虚作假、营私舞弊，以及其他严重违反教师职业道德的行为。990512</div>

							
							
							
							
