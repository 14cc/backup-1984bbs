<br />
一是因违法违纪行为受到纪律处分。<br />
<br />
二是在教育教学工作中散布违反中共党的路线方针政策、党的基本理论、国家法律法规等「错误言论」，对学生确立正确理想信念和政治信仰造成不良影响。<br />
