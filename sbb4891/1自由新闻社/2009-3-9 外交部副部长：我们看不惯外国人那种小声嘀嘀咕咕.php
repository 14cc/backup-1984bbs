 <br />
<br />
在说到中国人到美国华尔街“骑铜牛”的时候，吴大伟说这也是习惯，“如果你不让骑，就要竖一块牌子，上面写着‘禁止攀爬’”。武大伟说这是文化的差异，外国人有自己的礼节，我们也有我们的，不能说谁对谁错，要共同努力，互相理解。<br />
<br />
 <br />
<br />
看了这段报道，我首先想到我们副外长是在外交场合讲这段话的。因为在国际上，很多事情即便我们有错，也不能马上认错，我们是这样，美国和西方也是这样。否则，动不动就认错，还怎么搞外交？搞外交就是要为国家利益学会油头滑脑，或者强词夺理。<br />
<br />
 <br />
<br />
我们可以设想一下这样的场景，一位傲慢的美国人指责中国游客丢中国人脸了，破坏中国形象了，再看看我们外交部长的回答，那实在是太有才了！我甚至可以看到那位外国人目瞪口呆、哑口无言的样子。<br />
<br />
 <br />
<br />
怎么样？哥们，那可是我们的文化决定的，难道我们有什么错吗？我们还不喜欢你们那种礼节呢！两个人嘀嘀咕咕，我都听不见你们在谈什么，哈哈，我们的文化，你们知道吗？五千年历史呢，不过就是不适合你们那种乱七八糟的礼节，当然我们的文化除了比较习惯你们的小轿车、电脑和我们外交部长穿的这身非常得体的“西装” 之外，和你们的礼节格格不入，也许还有民主、自由、人权。<br />
<br />
 <br />
<br />
副外长彻底把我雷到了！<br />
<br />
 <br />
<br />
可是，且慢，各位，我们这位外交部副部长并不是在外交场合讲这段话的，这位被记者形容为游历世界各国、外交经验丰富的副部长可是在回答中国记者问题的时候说这段话的。他要回答的不是外国人，而是关心中国形象和自己脸面的国人，是那些希望中国人改掉陋习，提高素质的华人华侨，这样说来，副外长的话就大大的有问题了。现在，让我们简单分析一下他的回答吧。<br />
<br />
 <br />
<br />
第一，外交部副部长不是不知道一个基本的事实，迄今为止，批评国人到海外旅游时的一些陋习丢中国人脸的几乎百分之九十五以上是我们中国人自己，特别是实在看不过去的海外华人华侨。外交部请提供一个例子，这个世界上哪一个国家指责过国人出国丢中国人脸了？何况，即便丢中国人脸，关他们外国人鸟事？再何况，在有些傲慢的外国人看来，有些中国人本来就不要脸了，还怎么丢脸？再说，美国人连和他们格格不入的印第安人和非洲新移民的习惯都能够容忍，什么时候指责过中国人的陋习了？<br />
<br />
第二，所有指责中国游客在海外丢中国人、有损中国形象的陋习几乎都清清楚楚地写出来了，例如在公众场合大声喧哗，随地吐痰、乱丢垃圾和烟头、不排队、进入商场和电梯不掐灭烟头、用完抽水马桶不冲水等等，前段时间讨论这些陋习时，我没有看到一个人出来为这些陋习辩护过，可是今天出了一个高规格的辩护人，不过请问，有谁说过中国人聚在一起是“陋习”？怎么能如此偷换概念？至于“骑铜牛”应该竖“禁止攀爬”的牌子，我现在就在北京，今天正好经过天安门广场、好几个政府机关包括证监会和外交部门前，几乎所有这种场合的雕塑包括石头狮子面前都没有竖“禁止攀爬”的牌子，你问一下有哪一个中国人敢攀爬吗？如果不信，部长可以明天做一个试验，找人攀爬一个北京重要场合没有竖“禁止攀爬”牌子的雕像，看一下武警和保安是不是立即把你拉下来，训斥一顿甚至扣留下来？还有，我发现在北京公共场合的厕所里，很多都在马桶旁边写了“用完冲水”的提示，请问这也是文化的差异？有谁家里的抽水马桶旁边也贴了“用完冲水”的提示？<br />
<br />
第三，最让我郁闷的是，说来说去，怎么又扯到中国文化上了？我也在国内好多年，又在国外十几年，我可以负责任地告诉大家，所谓中国游客出国的那些“陋习”绝对是陋习，然而，却绝对不是中国人独有的陋习！不但第三世界人有这些陋习，英美国家的人至今都有！这些陋习是全世界人类共同面对并努力消灭的，可是，就在这个时候，我们的副外长却出来放话了，那不是陋习，那是文化差别！外交部副部长能够指出中国人在过去五年里讨论过的哪一个陋习是因为中国文化造成的吗？既然是文化的差别造成的这些陋习，现在据说要发扬光大中国文化了，那就让我们把这些陋习推而广之到世界各地吧！建议孔子学院开设学习陋习的教育课程。<br />
<br />
 <br />
<br />
我对这些陋习的看法<br />
<br />
 <br />
<br />
我记得十几年前刚刚出国时经历的一件事，有一次坐跨洋飞机，半夜醒来就和朋友聊天，由于坐飞机耳朵听不清，我们聊天的声音很大。结果一位外国女人从后面走过来，说，你们可以小声聊天吗？我们要休息。我当时很吃惊，感到很屈辱，但压低了声音。后来我发现我们的聊天确实吵醒了周围几排的乘客。<br />
<br />
 <br />
<br />
我后来多次坐飞机，也在飞机上碰到了一些大声说话的，半夜三更，我好不容易睡着，却被他们吵醒，真是讨厌。这时我想起了中国文化中最精华的一句话：己所不欲，勿施于人。这句话可是中国文化里的经典，也是孔子送给学生最重要的一句<br />
<br />
 <br />
<br />
话，可是恰恰在我们的整个教育里，始终没有人对我们好好讲解这句话。我现在还是大嗓门，特别是和中国朋友一起的时候比别人都大，但有一个原则，如果旁边有别人，我就会习惯性地压低声音，害怕影响到他人。<br />
<br />
 <br />
<br />
你知道现在在长途飞机上吵醒我的都是什么人吗？都是年轻的白种人，没有一个中国人！其实如果大家有在海外生活的经验，就知道，在没有中国人的场合，也有一些外国人（包括白种人）特别是年轻人大声喧哗，经常被人家白眼。一些很粗俗的白人也时不时随地吐痰，美国和澳洲的一些乡巴佬也有很多陋习，常常让我和其他一些外国朋友皱眉头。我们有时还私下讽刺他们。而同是中国人的香港人和台湾人，甚至包括主要是华人的新加坡，我注意到他们到海外旅游时，作为一个整体，要比我所见到的澳洲人和欧洲人、美国人更注重这些礼节，也更少惹人讨厌的陋习。<br />
<br />
 <br />
<br />
什么是中国文化？谁能说香港和台湾就比中国缺少了中国文化？如果说生活在海外的华人华侨离开中国文化太远，那我八十多岁的父亲应该更得中国文化的精髓。他到国外去看我，走进一个中国人的餐厅，发现那么多人，却那么安静，他感触良久，说，这才是久违的知书达理的中国人啊。<br />
<br />
 <br />
<br />
其实父亲身上竟然有那么多礼貌和品德都是我看上去那么陌生的，很多竟然和西方的一些很好的习惯相同。要知道父亲也是在1949年后受到过折磨的，还在农村挑过大粪，可现在条件稍微好一点，他竟然会在电梯里提醒年轻人不要吸烟，提醒我说话声音要小一点，他出门哪怕是散步十分钟，也会穿得整整齐齐。父亲是读私塾的，在1949前完成了整个教育。为什么他看到那些陋习就说那不是中国中文化呢？而且最近几年我接触好多位80岁左右的在1949年前接受了教育的中国人，我发现他们身上竟然有我在海外寻找了十几年、原来以为是西方人才有的那种品德和礼节，这又是怎么回事？副外长的年迈的父母如果健在的话，我建议他学习一下温总理，多和老人聊聊，体会一下真正没有被折腾过的中国文化。<br />
<br />
 <br />
<br />
当然我们不能不承认，很多我们前段时间讨论过的国人出国时的陋习确实更多的出现在中国以及其他一些第三世界游客身上。根据我的观察，我认为一些陋习（不顾他人制造噪音的大声喧哗、随地吐痰、不冲厕所、不排队等等）确实和一个国家的经济发展水平和民众的素质有关。如果我们拿GDP来说事，我们会发现人均GDP排后的国家，这些陋习更加严重。注意，这和游客本人的经济状况关系不大，而是和他来自那个国家的人均GDP有关。<br />
<br />
 <br />
<br />
但我也告诉大家我在海外的另外一个观察（以及我以前做了四年旅行社工作的感受），你现在不妨去找一下世界人均GDP排行榜，中国大概在一百零八位左右。我想告诉大家一个好消息，那就是虽然我们国人对自己在海外的陋习感到不安，但如果与和我们相近GDP国家的民众相比，中国人在国外的陋习远不是那么严重的，我们只是和GDP前五十位的国家国民相比，才显出巨大的差距。<br />
<br />
 <br />
<br />
这说明什么？这说明打开国门的中国人更倾向于严格要求自己，喜欢向高处走。我们是在和先进国家的国民相比，向那些实际上比我们人均富裕几倍、几十倍的国民看齐，有差距是可以理解的。而且，只要我们认识到这些差距，会很快追上去的。<br />
<br />
 <br />
<br />
还有一个重要的问题，那就是虽然这些出国反映出的陋习最早是被海外华人华侨发现的，然而，这并不是说国内的民众就没有认识到这些陋习。其实，我们前一阵子指责游客带到国外的陋习在国内早就遭到了围剿，而且正在努力消除，现在在高级一些的餐厅吃饭，你高声说话，影响了别人，你看看周围的人的目光和脸色吧。等公共汽车你不排队？等着别人吆喝你吧。随地吐痰？别人也许照顾你面子不会当场指责，但都会带点厌恶的表情绕道一两步。你去旅游景点爬上一些雕塑照相，你看看周围举相机的人会怎么鄙视地看着你吧。如果有人在天安门随地吐痰后被保安提醒，他会说我吐的是我们的文化之痰吗？<br />
<br />
 <br />
<br />
但我们的副外长说了，有些陋习是因为文化差异――这实在是让人诧异。副外长并没有说某一个“陋习”，而是有些。而我认为就在过去五年国民讨论的所有陋习，确实是真正的陋习，和文化无关，那副外长又是说的哪些陋习？莫非我们稍微有钱一点，去年又让外国人不敢干涉中国内政了，现在，竟然连陋习也要名正言顺起来？可这些陋习并不是外国人指出来的，而是我们国人对比外国发达国家的国民找出来的差距啊。<br />
<br />
 <br />
<br />
说实话，中国人那些陋习并不是什么了不起的大错，外国人都有过，只不过他们现在生活条件好了，教育更普及了，素质也相应提高了，于是慢慢改掉了一些，比我们先走了一步而已。作为一个GDP排名在一百位左右的国家，我们的民众素质相对来说还是相当高的，而且民间的力量加上海外的华人华侨的监督，我们民族还是比较自觉的，比较爱面子的。可是，现在出了个外交部副部长，一句话就为早被我们普通民众盖棺论定的陋习正名了，而且又是以文化之名。<br />
<br />
 <br />
<br />
有人说我们的文化不适合民主、自由和法治，我无话可说，就是想说也找不到地方说，但现在有人竟然又把文化和陋习扯到一起，我就不能不说了。我不知道这样对上不敬，对外交部副部长品头论足算不算一种中国的“陋习”，和中国文化是否有关？<br />
<br />
 <br />
<br />
杨恒均 2009/3/9　北京</div>

							
							
							
							
