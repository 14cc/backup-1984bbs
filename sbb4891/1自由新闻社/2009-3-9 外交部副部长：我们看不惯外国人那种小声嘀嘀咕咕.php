													<h2>外交部副部长：我们看不惯外国人那种小声嘀嘀咕咕</h2>
						
						
																											<div id="postmessage_48467" class="t_msgfont">羊城晚报3月8日报道 “引用最近热门电影《赤壁》里的话，对于金融危机，我是什么都‘略懂’一点。 ”在北京会议中心召开的对外友好小组里，全国政协委员、外交部副部长武大伟一发言，就逗乐了在场所有人。这位有着丰富外交经验的外交部副部长，在接受《羊城晚报》记者采访谈到国民出游形象问题时，坦率地指出，其实这些并不是“陋习”，只是大家习惯不同而已，需要双方一同沟通。<br />
<br />
大声说话是咱们的民族习惯<br />
<br />
近年来，常有媒体报道，中国人出游所表现出来的种种形象，“丢了中国人的脸”。可是武大伟却认为：“这是一种习惯而已。例如喜欢聚众、喜欢大声说话。你看，在机场里、餐厅里，大家聚一块说话的，都是中国人。可这就是一种习惯，我们还看不惯外国人那种小声嘀嘀咕咕、当着面还要相互咬耳朵呢。”<br />
<br />
在说到中国人到美国华尔街“骑铜牛”的时候，武大伟笑着说：“如果那个铜牛不让骑，就应该立一块牌子，上面写着：‘禁止攀爬’。”他幽默地说，这些是文化差异，外国人有他们的礼节习惯，我们也有自己的礼节习惯，并不能说谁对谁错，也不能单靠一方的努力，而是需要双方共同沟通，相互理解。<br />
<br />
中国的处境还是比较好的<br />
<br />
有着驻日本近20年的外交经历，武大伟表示自己已经历过了四次金融危机―――<br />
<br />
“第一次，是日本1973年的石油危机，我刚到日本那会儿，满大街都是霓虹灯，危机来后，就剩2/3了；第二次，也是在日本，泡沫危机，我看跟这次美国的金融危机如出一辙；第三次，是亚洲金融风暴，是索罗斯搞出来的。<br />
<br />
而眼下的第四次经济危机，武大伟表示是头一次在国内遇上的，“我认为，这次危机的本质是西方自由资本主义、自由市场造成的，而不是中国等新兴大国发展起来造成的。中国是受害者，发展中国家也是受害者”。<br />
<br />
经历了多次风波，武大伟处事不惊：“我觉得，估计不要太乐观，因为这次危机还没有见底，还在扩散蔓延，但有一点大家必须看到，我们中国的处境还是比较好的。”<br />
<br />
他打趣地说，你们看看，春节时候北京放鞭炮，可热闹了；我最近去了一趟广西和海南，那里的饮食业可兴旺了，“不像我当年在日本，”武大伟笑着说，日本人本来挺时髦的，结果危机一来，大家都不买新衣服了，街上见到的人，穿的都是三五年前买的、压箱底的衣服。“所以我们要对自己有信心。”<br />
<br />
武大伟其人<br />
<br />
武大伟风趣幽默，不拘小节，在采访过程中妙语连珠。<br />
<br />
在谈到礼节问题的时候，他好像想起了什么，说：“你看，你们也没自报家门，这就是不礼貌呢。上次有个记者采访我，还问我，您是谁，干什么的？他要不知道我是干嘛的，还来采访我干吗？”记者连忙递上名片，连忙解释：“刚才看到您跟另一个记者在聊天，我不好意思打断您。我是来自《羊城晚报》的记者。”武大伟一听，连连说：“《羊城晚报》，不错，挺火爆的！”<br />
<br />
当他知道记者叫“小莉”的时候，打趣地说：“这名字好，你叫小莉，我叫大伟。你就是‘勿以利小而不为’。” (本文来源：羊城晚报 作者：李晓莉、张小磊、鲁钇山)</div>

							
							
							
							
