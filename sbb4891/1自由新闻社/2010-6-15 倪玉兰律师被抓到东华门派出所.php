																											<div id="postmessage_685569" class="t_msgfont">女律师倪玉兰拍摄强拆现场被公安打残<br />
<br />
来源理&nbsp; &nbsp;理想休闲娱乐论坛 waxcy<br />
<a href="http://www2.55188.com/viewthread.php?tid=367641&amp;fpage=1" target="_blank">http://www2.55188.com/viewthread.php?tid=367641&amp;fpage=1</a><br />
<br />
北京女律师倪玉兰，拍摄强拆现场被公安毒打致残拘留判刑，她说在狱中一年能够大难不死，对比倪玉兰惨案前后的照片，从聪慧美丽的女律师形象到不能自己坐起、需要*双拐移动寸步的上访女士……，惨痛遭遇令人长叹。<br />
<br />
<br />
但是，她屡遭毒打，长期入狱，仍然不向暴力政府低头，铮铮铁骨令警察们也不得不称呼倪玉兰为“江姐，江雪琴”。<br />
造成这个惨案的原因是什么？我们为此采访了失去了健康和工作现在是一位上访女士的倪玉兰，她说：“这个政府不遵守法律违法乱纪，所以当我要求他们遵守法律的时候，他们就怒火万丈，向我下黑手。在中国这么大个土地上，这个政府这么无法无天！我一定要讨还一个公道，我们要向世界人民呼吁：中国是没有人权的，这个政府借著奥运，想怎么抢夺老百姓的财产就怎么抢，一口价，你不给，他们就打你、拘、判，卑鄙无耻，<br />
<br />
<br />
<br />
倪玉兰，今年43岁，从事律师职业18年。因为北京西城区政府对拆迁赔偿感到不合理的居民房屋强行拆迁，被拆迁户赵申（音）邀请左邻右舍和倪玉兰等看政府如何强拆，希望他们作个人证。<br />
<br />
2002年4月27日，倪玉兰为了避免被拆迁办看见躲在人群中偷偷拍照强拆现场时，却被两名政府拆迁人员发现，他们把她推倒在地并抢走了她的相机，曝光了胶卷，西城区政府强拆人员叫来公安，命令公安们把倪玉兰拖到新街口派出所里。<br />
<br />
<br />
公安拖著倪玉兰进到了派出所，轰走了当时办公室里的所有保安人员，副所长赵玉经（音）把倪玉兰五花大绑扔在地上以后，所长谢立国、卞卫东、程光远、张研文（音）、杨林（音），石建星（音）等八名公安每人一个部位一边对她拳脚相加，一边骂道：“让你T.M.D多管闲事。” 倪玉兰痛苦的发出高声惨叫。<br />
<br />
记者：“请问八名警察使用什么器械打您？”<br />
<br />
倪玉兰：“他们不用器械，他们都是经过训练的人，他们用脚踢，用胳膊肘凿，我的大腿根和小便的部位都让他们给损伤了，大腿根的筋被他们弄断了一根，小便尿血。他们让你伤了，还说不出道不出来，特别流氓。”<br />
<br />
打了十五个小时后到次日凌晨三点，倪玉兰已经大小便失禁，数次昏迷不醒，公安以倪玉兰『照相』、在派出所被打时发出高声惨叫给她扣上『大嚷大叫』、公安们还编造受刑中的倪玉兰『踢打警察』判她拘留处分10天，这时，倪玉兰已经残废不能行走，拘留10天到期公安不放人继续拘留，据有关人士分析：公安们恐怕她受伤未愈出去带伤申冤，他们等到她的伤处变为旧伤后才放人，结果总共拘留她75天，拘留的全部过程，她没有得到公安任何拘留处分的书面证明。<br />
<br />
在拘留期间，公安还意欲斩草绝根，将倪玉兰关进精神病医院以图一劳永逸，他们声称倪玉兰有精神病，偷偷给倪玉兰作精神鉴定，因为精神病鉴定结果说倪玉兰是正常人，公安最终不得不释放了倪玉兰。<br />
<br />
这时她已经落下了残疾：左腿不能弯曲行走，颈部腰部和左前胸严重变形，胸前鼓出个包，躺在床上时需要人的扶助才能坐起。<br />
<br />
<br />
在倪玉兰释放后，她向各个部门反映冤情，没有得到公正处理反招政府追究倪玉兰的责任。<br />
<br />
从2002年8月开始，倪玉兰向更高级的行政上告冤情，开始了她险恶的上访历程。<br />
<br />
2002年9月27日，在倪玉兰向北京市人大常委会反映新街口派出所施行酷刑时，新街口派出所二十六七岁的警察刘俊杰、三十多岁的警察闫修建各自提起她的头部和脚部，往地上多次墩她，他们活生生的摔断了倪玉兰的尾骨，并且将她关押判刑。<br />
<br />
倪玉兰说：西城区检察院捏造证据，采用架空的证人作伪证，西城区法院在不通知本人、没有律师的情况下对倪玉兰进行审判，法官米德志以&quot;妨碍公务&quot;的罪名判处倪玉兰有期徒刑一年，这期间她饱受折磨，出狱后，1米65的人，从标准体重100斤瘦弱到70多斤。<br />
<br />
警察还搜走倪玉兰包里的600元钱和一个金戒子，倪玉兰录的警察违法办案的录音带、笔记本，材料等，倪玉兰说：“他们随便拿走你的东西，比如这个钱和金戒子，他们根本就不给你登记，据为己有，你凭什么拿这些在押人员的财产啊，他们有什么权利？”<br />
<br />
因为残忍有功，新街口派出所警察刘俊杰还获得去外地旅游的奖励。倪玉兰说：“这都是他们的所长唆使下面的警察干坏事，因为他做了违法乱纪的事，所以他要他的下属来遮掩他的犯罪行为。”<br />
<br />
出狱后，因为有过判刑，倪玉兰失去了工作。但律师的经验使她了解法律程序，她调查了存于法院自己被判刑入狱的的复印案卷，因为法院案卷不能复印，倪玉兰就从案卷中一字一句抄录了案卷内容。<br />
<br />
倪玉兰据此上告北京最高人民检察院她被新街口派出所公安施以酷刑、检察院和法院还使用架空证人作案卷的伪证，然而，两年过去了，北京最高人民检察院到现在为止对此不闻不问，不给予立案，反而，还经常被威胁殴打，甚至高检的官员还勒索她，“人民”高检欺压人民已不是稀奇事了。<br />
<br />
2003年7月18日，在倪玉兰接受了电台采访后的第三天，在公园里她再度被新街口派出抓住并被关押三天，罪名是『防碍公共秩序』。倪玉兰气愤的说：“在公园里怎么能防碍公共秩序？而且关押的三天期间，他们不给饭吃，不让去厕所，他们用非法的手段对我进行折磨。”<br />
<br />
对于倪玉兰屡次上访，西城区公安分局信访的一位姓薛的警察总是推脱她说：你回去再等两天，倪玉兰说：“结果我们等了两年也没有立案，至今没有任何结果。他们就是该立案不立案，该解决的问题不解决，比如说拆迁工作人员，他没有权利抢我相机，他既然实施了抢夺相机这个行为，他就是犯法了，所有，新街口派出所是在2003年7月28日，才形式上受案，一直到现在没有立案，我们一直在要求他们履行法律职责。”<br />
<br />
政府还一直拒绝给倪玉兰做法医鉴定，她领不到残疾证，购买不了残疾车，坚持了两年天上访是丈夫用自行车带著倪玉兰四处奔波。在交通警察看到他们夫妇骑车带人要罚款表示这是规定时，倪玉兰一看到警察就气不打一处来，说：“规定多著了，还规定保护人民财产和安全呢，那我强制拆迁现场照照相，你们警察凭什么就把我打的残废了？”<br />
<br />
当警察在询问下知道倪玉兰还不是为了自己拆迁是为了别人拆迁照相被迫害如此，他抓起倪玉兰的手不停，如果不是有许多人围观，几乎要吻上了。倪玉兰说：被拆迁户有许多也是警察家庭，可是他们不能上访，如果上访，就会失去职业，不能养家糊口，所以他们也深有痛感。<br />
