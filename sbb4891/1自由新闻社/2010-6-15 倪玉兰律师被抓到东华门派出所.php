													<h2>倪玉兰、倪玉兰</h2>
						
						
																											<div id="postmessage_684546" class="t_msgfont">文章来源：崔卫平《倪玉兰、倪玉兰》<br />
我此前没有听说过倪玉兰。这位漂亮、坚毅的女律师，1978年考入北京语言学院，在中文系获本科学位，后来又获得中国政法大学的本科学位。她年轻时在鲜花花丛中笑容满面的照片，我们许多人的相册中都有相同的一张。1986年开始她从事律师工作，曾在中国国际贸易总公司担任法律顾问、正义律师事务所担任律师。而如今的倪玉兰只能在轮椅上坐着，而且露宿街头――在朋友的资助下，她们夫妇在皇城根遗址公园支起了帐篷，那就是她的生活和工作天地。她是这部新近完成的纪录片《应急避难场所》的主角，导演何杨。<br />
<br />
何杨是北京电影学院的毕业生，曾经在北京电视台做纪录片栏目的编辑。2003年秋天，是何杨找到我，介绍了胡杰先生的《寻找林昭的灵魂》。<br />
<br />
在进入正题之前，先说几句。当然我们对于受害者抱有天然的同情心。那些人们无缘无故祸从天降，生命受到巨大戕害，家庭财产被扫荡一空，在火车上睡觉被人突然戳几刀，在等红绿灯的时候遭遇撞车，报纸上经常登有这样的事情。对这样的无辜的不幸者投去关注，表明我们身上人性的力量尚存，当我们体验到同情时，是对于我们自身人性的肯定。同情是我们作为人的“底线情感”。<br />
<br />
然而有另外一些人，她/他不仅仅是被动的受害者，她/他们是在这个世界中的行动者，他们对于这个世界远非无动于衷，而是向来积极投入和热心参与。某种灾难的降临，甚至是他们自身的所作所为引起的，是他们自身行为的结果，是因为他们的争取、抗争、不屈――他们以自己的行为，释放了极富意义的精神和道德价值。因此，在她/他们承受着自己行为结果的同时，这个世界也因为她/他们，呈现了不同的面貌，赋予新的光彩。这就是古典意义上的“英雄”。<br />
<br />
英雄有看得见的行为。她/他们对于这个世界表现出慷慨大度，她/他给予这个世界的，远非这个世界提供给他们的要多得多。我们对于英雄，所能报之的，就不仅是同情，而是尊重和敬重。如果说，同情触及我们身上柔弱的部分，那么，尊重和敬重这类情感，便焕发了我们身上富有力量的部分，召唤着我们本身对于这个世界的决心、期待、向往和行动。<br />
<br />
而倪玉兰就是这样的当代女英雄。因此，当我们传颂倪玉兰的故事，不仅是她如何经历了酷刑，被五花大绑地捆起来之后，绳子还要往上拉，只听得“肋骨嘎巴嘎巴响”；不仅是她被人用膝盖死命挤压她身上的各个穴位，表面上不露痕迹，但是却疼痛得让人生不如死；不仅是她被摔碎的水杯割大腿根，“它是很钝的，不像刀子一割就是一个口子，他割了我很多次”，她说，乃至很多年之后才能痊愈；也不仅是她最终被打得腿萎缩，至今无法站立。那是2002年的事情。<br />
<br />
片中的倪玉兰律师对着镜头清晰地记得申奥成功是在2001年7月13日，接着就是一场大规模的拆迁。2002年4月23日拆到西城区大四条55号赵家，赵是倪律师的邻居，作为街坊她与很多人都去声援了。倪律师在人群中拿了一部相机，悄悄地在一定距离之外进行拍摄。拿着机器是一件危险的事情，因此，在众多声援的人们中，只有她一人被抓进派出所。本来说是治安拘留十天，可是一直没有放她，直至2002年11月27日，她因为“妨碍公务罪”被判刑一年，同时吊销律师执照。<br />
<br />
2010年6月3日下午，我去皇城根遗址公园看望了倪玉兰律师，她告诉我，这个所谓“妨碍公务罪”写在字面上的指证有两条，一条是“在拍摄现场拍照”，另一条是“扰乱办公秩序”（即在办公室大喊大叫）。后一条即是前面所说的，在对她施行酷刑时，她疼痛难当，只有大声喊出自己的痛苦。难道一个人在经历非人的痛苦时，不允许叫出声来？面对这么一个柔弱的美丽女人，需要什么样的心肠，什么样的铁手，才能够打得下去？被她“扰乱”的这个“办公室”，不是她自己要来的，她不可能自己跑到某个公共机构去妨碍别人的公务，而是有人将她从街坊邻居的拆迁现场捉到了这里。对一个手无寸铁的女性，如此挥动拳头、大动干戈，这有什么威风而言呢？<br />
<br />
这个为之让路的“奥运”早已经举办完了，那些因为奥运而遭遇强拆人们的痛苦，也渐渐淡出人们的视线。这之后发生了许多事情，因为拆迁引起的矛盾在今天越演越烈，引起了民众更为广泛的不安和抗议。但是不能因为早些时候发生的事情，就可以淡化和忘却。实际情况是，正是因为更早时候人们的容忍，觉得那些事情离自己还很遥远，有人甚至会觉得为奥运所做的一切事情和任何方式都是可以接受的，于是事情一点点发展成为今天这样。<br />
<br />
从此倪玉兰的噩运就开始了。03年刑满释放之后她开始为自己的不平遭遇上访。她从一名“律师”，进而成为“维权律师”，进而成为“访民”。而她又是一名特殊的访民，她是他们当中精通法律、有智慧、有见识的一位，因而赢得了众多访民的信任和爱戴，她替他们出主意，帮助他们理性地、和平地争取自己的利益。这固然会招来进一步的记恨，被说成是“出馊主意”。<br />
<br />
2008年，倪玉兰位于西城区的家遭到了数次强拆，倪玉兰再次被弄到派出所，对她的殴打、虐待仍在继续。我不想重复倪律师的全部遭罪，如果将它们写下来，我自己就要再承受一遍，这个令人痛苦。影片中关于这方面的内容有叙述，总的来说，还是比较克制的。不能回避的是后来导致她再度入狱的原因：在她已经被警察拽掉裤子的情况下，一名警察当着她的面掏出了生殖器，把尿撒到她身上。她不能喊“警察耍流氓”，只能喊“警察打人”。来人之后，这个警察反称她“踢打警察”，因为这一条，她又被判入狱两年，今年4月份才从监狱出来。片中的导演也是采访者何杨问道：这个人需要拐杖才能够站立，她怎么可能将警察踢伤？<br />
<br />
同样又是“妨碍公务罪”！这个罪名放在别处，可以令当别论；然而放在倪玉兰律师这里，这甚至不是对于她的惩罚，而是对于她的加冕――这位女公民除了与警察起冲突外，她在其余一切方面，都是经得起考验的，至少从法律的角度看是纯洁无瑕的。她两次被带到派出所都是在邻居和自家的被拆迁现场。无论如何，倪律师在家园被拆的现场，都不构成“犯罪”，不构成法律惩处她的理由，不构成让她去坐牢。拍照能算吗？那么余下来，她主要的“犯罪”地点，便是在派出所里？而堂堂“派出所”，怎么会成为公民触犯法律的场所呢？怎么会成为这么危险的地方，致使一个公民不止一次地在此获罪？<br />
<br />
这个倪玉兰特别了不起。她的“妨碍公务罪”，换句话来说，是对加在她身上非人做法的抵抗，是不屈服于那些不讲理的势力，那么，她就是“不服从”的罪、“不低头”的罪、“不配合”的罪。她丝毫没有危害社会，没有危害他人，她没有任何这方面的记录，她只是被认为对警察（及他们所要执行的公务）构成了“危害”。一句话，小小倪玉兰，是因为与穿制服警察的“犯难”，才将“罪犯”二字烙在了自己身上。<br />
<br />
她告诉我，08年被收进监狱时，作为“囚犯”她们要给管教写信，称对方为“尊敬的警官”，她却写道，“我是被称作罪犯的，倪玉兰，我本无罪”。与以往每回一样，这再一次给她带来不服从的惩罚。她的拐杖被拿走，多次被迫在地上爬行，受到当众羞辱。“假如你不是这样做，他们便不会那样对你，对不对？”我问。她第一次表现出茫然的神情，好像不知道我在说什么。<br />
<br />
