													<h2>受审因说“放屁”，青年遭法警当庭“拍脸”</h2>
						
						
																											<div id="postmessage_244865" class="t_msgfont">武汉晚报9月16日报道 昨日，阳新县白沙镇珠林村68岁的王月红向媒体投诉，她的儿子开庭审理时，遭法警掌掴。<br />
<br />
王月红称：去年2月25日，儿子马某涉嫌绑架被抓，先后三次审理。本月14日下午，大冶法院第三次庭审时，儿子对公诉人几次打断他的陈述很恼火，脱口说了一句脏话“放屁”。王月红称，6时许，一名法警上前殴打儿子马某，导致现场一片混乱，庭审中断了10分钟左右。<br />
<br />
得知王月红投诉后，大冶市法院昨日调出当日的开庭录像。记者发现临近庭审结束时，确实有一名未穿警服的男子将手伸向马某的肩膀处和脸部。经证实，该男子系法警郝某。<br />
<br />
<strong>该院负责人表示，他们及时进行了调查。当时，被告情绪有些失控，法警郝某为了维护庭审秩序，上前进行了必要的训诫，拿手“拍”了对方的肩和脸。殴打一事绝不存在。</strong><br />
<br />
律师说法：湖北省律师协会刑辩委员会副主任、湖北今天律师事务所王万雄昨日说，训诫是语言上的告诫和教导。法警上前“拍脸”，不仅不是训诫，还严重违反了庭审纪律，更与《刑事诉讼法》的相关规定背道而驰。 (本文来源：武汉晚报 作者：王建兵)&nbsp;&nbsp;<br />
