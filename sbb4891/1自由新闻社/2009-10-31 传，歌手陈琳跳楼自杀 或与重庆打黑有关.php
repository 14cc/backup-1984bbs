													<h2>内地女歌手陈琳跳楼身亡 享年39岁</h2>
						
						
																											<div id="postmessage_308388" class="t_msgfont">新浪娱乐讯 今日(10月31日)凌晨，内地女歌手陈琳的尸体，在东坝奥林匹克花园三期701号楼被发现，据称她是跳楼自杀。新浪娱乐已向陈琳自杀地所属派出所求证，对方未否认。此外，陈琳的好友黄先生也向新浪娱乐确认了该消息，目前正在为她料理后事。有好友称，陈琳死前为情所困。<br />
<br />
　　具体死亡时间无法确认<br />
<br />
　　新浪娱乐赶到陈琳跳楼所在小区，小区居民透露，今早六点有人遛狗时已发现陈琳的尸体。报警后有警察前来处理。陈琳的尸体直接砸入一楼花园，导致花园受损。但无法核实其具体跳楼时间。陈琳的好友黄先生向新浪娱乐独家确认陈琳已死亡，并自称正在为其料理后事。<br />
<br />
　　有友人称为情所困<br />
<br />
　　据《法制晚报》报道：今晨7时许一名女青年被发现死在一居民家的一楼小花园内。死者仰面躺在一层居民自建的小花园内，年龄20多岁，身穿灰色外套、牛仔裤，下巴处贴有一块口罩大小的白色纱布。999急救人员赶到现场后，确认她已经死亡。上午11时，一名自称死者朋友的年轻女子跑过来，当场放声大哭，她说，自己住在奥林匹克花园一期，之前死者因感情问题来找自己，目前暂住在701号楼九层。“神经病！疯了！怎么这么想不开！？”她对着毫无声息的朋友边哭边骂，后来她对警察说，昨天晚上的情况自己不知情，听说这边出事儿了才跑过来的。<br />
<br />
　　据了解，文中所称女青年，即为陈琳。<br />
<br />
　　经纪人称陈琳电话无法接通<br />
<br />
　　今天下午北京某著名经纪人从警局朋友口中得到消息，称陈琳在朋友家中跳楼身亡，他们刚刚出警归来，但具体情况和细节并没有透露。该名经纪人随即向圈中记者询问详情，导致陈琳跳楼自杀的消息在圈中很快传播开来。新浪随即联系陈琳的经纪人刘梅。刘梅称她也是今天下午记者电话她才得知此消息，但她并没有接到警方的任何通知。她也试图联系陈琳，但电话始终无法拨通。<br />
<br />
　　前夫暂无回应<br />
<br />
　　陈琳曾经是音乐圈著名厂牌竹书文化的签约艺人，并跟老板沈永革有过十多年的恋情。1994年，陈琳与沈永革相识，一年后沈永革创办北京竹书文化，陈琳不仅成为他的签约艺人，也成了他的妻子，但十多年的恋情最终在07年走向终点。09年7月，有四川媒体报道了陈琳与音乐人张超峰再婚的消息，证实此前陈琳与沈永革离婚的消息不假。新浪娱乐随即也联系了沈永革，但他的电话也无人接听。据圈中一位对陈琳比较了解的音乐人透露，自从她与前夫沈永革离婚后一直非常不顺，情绪上相当低落，近两年已逐渐淡出媒体视线。此外，他还透露，今天(10月31日)是沈永革的生日。<br />
<br />
　　警方不做回应<br />
<br />
　　另据新浪娱乐北京朝阳区东坝派出所了解到，警方承认今天下午在他们的辖区有人跳楼，但具体情况以及当事人的详细情况不便透露。JZB/文<br />
