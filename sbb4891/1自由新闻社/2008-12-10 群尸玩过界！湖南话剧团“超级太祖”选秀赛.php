目前，国内红色旅游演出市场风生水起，有重庆的《红岩联线》、延安的《梦回延安保卫战》、井冈山的《井冈山》，而作为毛泽东故乡――韶山的红色演出市场却至今空白。省话剧团就是看准了这一市场空白。《韶山升起红太阳》将充分利用韶山独特红色资源优势，挖掘红色历史文化内涵，节目中安排了一些和韶山红色文化相关的情景，中间还有游客与特型演员互动的环节，以增强红色旅游的观赏性。<br />
<br />
为了找到最佳的毛泽东饰演者，湖南省话剧团联合本报自今年10月始，面向全国发出招募令，征集毛泽东特型演员。截止目前，已经有130多人报名，20多人参加了剧组的试戏。新闻发布会上来自全国各地的“毛泽东”代表14人登台亮相。毛剑锋表示，“这部剧将突出表现毛泽东一代伟人最普通的一面。采用大量音乐、舞蹈，结合话剧表演和朗诵，突出湖南的、韶山的音乐、舞蹈特色。”<br />
<br />
此次招募同时还在腾讯娱乐启动，本月15日网络投票结束。省话剧团将汇合专家、网友投票等多方面意见于本月20日正式公布毛泽东特型演员人选。<br />
