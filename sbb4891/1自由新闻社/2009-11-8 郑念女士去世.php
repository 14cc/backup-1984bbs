																											<div id="postmessage_319840" class="t_msgfont">贵族格调：看海派贵族的兴亡<br />
<br />
一提到海派贵族，大家自然想到的是解放前大上海的旗袍、夜歌、舞厅等形象。好笑的是，旧时代的海派贵族们，其实很少涉足百乐门等地方。如果要举出一个海派贵族代表性人物，人们最容易想到的是宋氏家族。有意思的是，宋家王朝建立者本非上海人。然而，宋氏家族在上海滩的起飞，的确代表了中国新文化后海派文化贵族的典型。<br />
<br />
上海在1840年鸦片战争后开阜，仅仅是个渔村小镇，和改革开放前夕的深圳差不多。随着列强的进入和地理位置，上海开始了飞速发展，巨大的经济基础是海派贵族们的财富来源，独特的教育环境和历史地位，让海派贵族们在较短时间内走向了辉煌。这个特殊历史时代，造就了一批不是上海人的“海派贵族”。<br />
<br />
和京派贵族不同，海派贵族不太讲究出身。即便是宋家王朝的缔造者，曾经不过是一介平民，丝毫没有皇宫大臣的背景。上海人，其实是个很模糊的定义。解放前的上海，是一个类似纽约一般的大熔炉，全中国乃至全世界的人们，都可以来到上海，包括被苏联赶走的沙俄贵族到受迫害的犹太人，很多后来都把上海当成了自己的家园，希拉里-克林顿夫人的父亲小时候，就曾在上海犹太学校避难。这所犹太学校建筑如今归属上海教育局。<br />
<br />
老上海正是以这种特殊的环境，造就了中国的海派文化和海派贵族。<br />
<br />
京派贵族由于被列强欺负得太不起头，因此看不起上海贵族对西方的亲近。这种对海派文化的看法一直延续到了解放后，成了很多上海人被打倒、被批判的罪证。<br />
<br />
其实，海派贵族的来源，并非都是像宋家那样的传教士家庭。很多海派贵族本身是脱胎于传统的江浙甚至全国各地的贵族一群或者是事业有成之辈。他们有的因为商业、战乱（如东北）、政治威胁（如鲁迅）、事业（如梅兰芳）等等原因来到了上海滩。半殖民的环境，完全开放的口岸，让这些来自九州四海的中华贵族和能人，纷纷踏上了上海这片冒险家的乐园。<br />
 <br />
<img src="http://img1.qq.com/luxury/pics/14366/14366249.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
传统的“三代出一个贵族”的理论，在老上海被缩短了。海派贵族之所以能短期内成为贵族，部分因为海派贵族本来的根还是的中华传统贵族。只不过和京派贵族不同的是，海派贵族们适应了新时代，在事业上取得相当的成就，在文化上实行了蜕变。他们的经济基础，来自于他们的教育和社会能力。在新环境下，他们比京派贵族有着更加稳固的经济支柱。在文化上，他们更加适应当时的新时代，最终成为时代浪尖的领头羊。<br />
<br />
很多功成名就的人士，很快就被认可为上海上流社会的一员。这点和京派的皇宫大臣血统论截然不同。上海的贵族定义更广泛，可以说在老上海，不少海派贵族有着一些“范进中举”式的传奇故事。<br />
<br />
海派贵族很多来自不同社会阶层，从事不同的行业，并非一脉相承。无论是呼风唤雨的宋家王朝、大上海风景画的作家张爱玲、上海滩大亨犹太人哈同、建立上海公交线和雷斯顿理工学院的英国人雷斯顿、还是京剧大师梅兰芳，或者写作方面的女神童琼瑶，他们以后在社会上地位的变化，都是“海派文化”海纳百川的传奇代表。<br />
<br />
贵族的定义，远不是有钱就是贵族。这点无论是明清时代的皇族、山西银行家还是后来的徽商、浙商等家族，都非常清楚的。要维持家族的事业和社会地位，要成为真正的上流人物，教育是必须的。这是中国历史数千年的教诲。而海派贵族，则完全继承了这个中国的传统，在子女的教育上投入不菲，而且极其开明。<br />
<br />
海派贵族没有血统论，也不讲你是中国人还是老外，他们看中的，是你的个人。你的事业贡献，你的家庭门风，你的教养，你的财富。财富仅仅是其中一部分。大上海的很多成功人士，虽然财富上谈不上“贵”，但他们能来往于上流社会，并被海派贵族接纳。这里，教育是关键的关键。<br />
<br />
上海进入20年代后，雄厚的经济基础促使了教育和文化高速度发展。当时上海的高等教育已经比肩日本，进入世界一流行列，著名的上海圣约翰大学被誉为东方的哈佛，上海交大、同济、震旦、沪江、雷斯顿理工等一大批出色的大学，各自根据美、德、法等列强的办学理念建立，最后融会中西，为老上海和海派文化的辉煌，打下了扎实的教育文化基础。除了大学，当时上海还有一批著名的中学，如培养了宋氏三姐妹、张爱玲等的中西女中等一大批教会、本土的中学，也是名人辈出。高超的教育水准，是老上海贵族们持续辉煌的最大本钱和标志之一。可以说，教育水准决定了一个人在当时上海的社会地位。听上去很有些“范进中举”的味道，也可以看到海派贵族的中国之根。<br />
<br />
<img src="http://img1.qq.com/luxury/pics/14366/14366247.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
海派贵族往往被人指责“崇洋媚外”，看的是好莱坞电影，吃西餐、跳社交舞等等。一般认为是上海被全盘西化的结果。<br />
<br />
但只要了解真正海派贵族的人都知道，海派贵族其实内心非常中国化。他们这些人，拿起钢笔写出一串外语，提起毛笔就是一行漂亮的楷书。虽然上海的中等和高等教育机构的西方色彩很重，但到了家里，浓厚的传统风俗习惯得到绝对的尊重。中国的各个节日、祭祖、例行到长辈家请安等，绝对不能马虎。即便是已经入了基督教的宋家，也不能随便以基督教来改变中国的很多传统习惯。<br />
<br />
老上海贵族们对传统文化的传承方面，作出了巨大贡献。京剧各大派到了20世纪，几乎都得到上海贵族们的大力支持，梅兰芳移居上海后，甚至与当地一家地产金融商联了亲。须生大师孟小冬女士和上海滩黑帮三巨头之一杜月笙的传奇故事，也是一段美谈。“越剧十姐妹”的辉煌，很大程度上得到了浙籍贵族们的大力捧场和财务支持。传统国画的北齐南张、宗教的高僧大德，以及在文学、戏曲、音乐等方面，海派贵族对传统文化的支持不遗余力。如京剧在老上海的普及之广泛，就连黄金荣、杜月笙等人都可以彩装上台一唱。解放后，上海金融界人士经常造访上海昆剧团，财务支持，演出捧场，看着上昆团的“七梁十柱”在“传”字辈老一代的培养下，逐步成长为中国昆曲界的一代大师。<br />
<br />
对于国外的新玩儿，如电影和时尚，老上海人也不让欧美。原本保守的京派贵族旗袍，在上海裁缝和海派贵族独特的品位改造下，逐渐变成了闻名遐迩的改良旗袍，风靡近百年，依然风头不减。2000年香港电影“花样年华”更把上海旗袍风刮向了全世界。从警惕、接纳、消化、最后中西合一，海派贵族们独创的品位，变成了老上海的风尚标志，使得上海成为了旧中国的时尚之都，风靡东亚各国。音乐方面，民歌小调结合爵士乐形成的“上海夜歌”，是中国流行音乐歌曲的开始，影响了中国流行歌曲乐坛数十年。<br />
<br />
上海贵族们的确喜欢欧美的东西，这点不可否认。可并不是所有外国人的东西，都能在上海站稳脚跟。在20世纪初，日本虽然国力赶超了中国，但随着上海的不断发展，日本的一切就不再入上海贵族的眼。日本制造的商品由于质量差，因此被上海人讥笑为“加庞货”而拒绝。<br />
<br />
<img src="http://img1.qq.com/luxury/pics/14366/14366248.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
与其说海派贵族崇洋，不如说他们只喜欢真正的、最好的、最高档的东西。当上海开始赶超先进国家的之际，他们会毫不犹豫推崇自己独特的品味和风尚。海派贵族们对老上海电影业、时尚、以及传统戏剧、艺术方面的巨大支持，就能看出，处于全盛时期的海派贵族，已经在融合中外的道路上，创出了一条与众不同、独具一格的文化现象。京城新文化运动创造者们一直梦想出现的一种中国新文化雏形，在上海强势出现。<br />
<br />
如果说要用一个行业来代表上海的辉煌，那就不得不提到老上海电影业。明星在上海和传统的戏子概念已经完全不同了。上海贵族们和电影明星的来往，往往成为当时八卦新闻的材料。对于电影业的尊重，连带着海派贵族优待电影明星们，就连所谓传统戏曲的戏子到了上海，得到了前所未有的崇高社会地位。看低戏子的老观念，在上海首先消退。在这种开放、前沿的观念下，老上海电影界汇集了来自中国各地的精英，左翼、写实、言情、清宫、武打、恐怖、悬念、喜剧、戏曲、古装、现代等各种具有中国风格的电影模式，在老上海影坛取得了巨大成就，一直影响了两岸三地的影视界数十年。<br />
<br />
在大陆，海派文化和生活方式由于其强大的辐射力，一直被众多人所追崇，成为了后来所谓的“资产阶级生活方式”的代表。上海贵族们很多和工商、金融、教育、文化界关系密切，因此他们的贵族文化也深深影响了整个上海社会和很多其他地区。即便是在板刷头、白衬衫、蓝裤子、黑布鞋统治的1960、70年代中国服装最为单调的时期，上海时尚总是能够在全国各地城市中独树一帜，在可怜的空间里，尽可能地在黑、蓝、白的世界中翻出一些小小的时尚小浪花，给穿衣服人增加一丝特有的色彩。以至于那个年代的中国人，都有种下意识的感觉，觉得到上海就能买到比当地要好看得多的衣服。尽管那个时候“时尚”这个词早就被大众遗忘。<br />
<br />
解放后，公私合营，上海贵族们的产业基本上交了国家，丧失了任何参与产业管理的资格。他们空有一身本事和一腔报国的热血和激情，但由于自己的出身，最终报国无门，只能安然退出了新中国的建设大潮，躲进了自己的世界。<br />
<br />
靠着公私合营后的定息和自己的家产，海派贵族们还能维持自己的资产阶级生活。好莱坞电影消失了，西方的信息没有了，海派贵族们还是以自己的方式，继续生活在一个截然不同的世界里。50年代到60年代初，海派贵族们中居然还可以看到穿着白色婚纱的结婚照。在自己的客堂里，年轻一代人放着家中解放前收集的老唱片，品尝着舒适优雅的生活。虽然中国社会已经出现了巨大变革，海派贵族们与众不同的生活和文化意识成为了批判的公敌。但解放后短暂的和平时代，让残留在上海的贵族们还能保留一丝自己的生活。<br />
<br />
