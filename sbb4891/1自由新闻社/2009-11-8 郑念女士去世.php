													<h2>怀念一个美人</h2>
						
						
																											<div id="postmessage_319621" class="t_msgfont"><a href="http://www.ipencil.org/?p=983" target="_blank">http://www.ipencil.org/?p=983</a><br />
《生死在上海》Life and Death in Shanghai书评：<br />
<br />
　　以下的情景在许多时间和空间里司空见惯：<br />
<br />
&nbsp; &nbsp; 　一个富裕的人家里，夏天炎热的晚上，女主人坐在自家静静的书房里看报。台灯的光照下，柔软的沙发，织锦缎的垫子，墙边装着中文英文书籍的书架。佣人前来通报有客来访，然后，会客厅里，主人和客人聊着天，佣人端来用精致瓷器盛的茶和英国式薄三明治……<br />
<br />
&nbsp; &nbsp; 　　但在1966年夏天的上海，也会有这种生活吗？<br />
<br />
　　是的，虽然在那时，在那个城市里，能有这样生活的人非常少，但这毕竟是真实存在的。可是，在我的印象里，1966年的中国，即使是在上海这样的大城市，也似乎断不应该能够存在这种生活景象的。我以为，在那个疯狂的年头以前好几年，这样的人及其生活方式就应该从中国大陆消失了。看来我还是不了解历史。<br />
<br />
　　这是这本书给我的第一个惊讶。<br />
<br />
　　这是一本自传体小说，接下来，故事逐渐展开。<br />
<br />
　　以下是女主人公的个人背景：一个知识女性，出生在北京，燕京大学毕业，然后赴英国留学――到英国政治经济学院。在那里，1935年，她遇到了自己未来的丈夫，也是一名中国留学生。他们结了婚。她丈夫成为国民政府的外交官，被派往澳大利亚使馆。在澳大利亚，他们的女儿诞生了。后来，她丈夫回到了中国大陆，在上海继续担任外交官。1948年10月1日，她和女儿也来到了上海。<br />
<br />
　　1949年以后，他们一家选择继续留在上海。她丈夫不再是外交官，转而出任壳牌公司上海办事处总经理，直到1957年患病去世。壳牌公司派来了新的总经理，英国人。应壳牌公司邀请，她成为新任总经理的助手。一直工作到1966年壳牌公司离开中国大陆。<br />
<br />
　　这家人的祖父是中国近代史上的一个著名人物。因此，他们有着丰厚的家产，在国内外的银行都有存款。也因此，他们才能在1966年的时候还保有那种生活方式――独立的三层住宅，男女佣人和厨师、园丁。仅在大陆银行，她的存款就数以万计。<br />
<br />
　　当然，随后发生的事情对于中国人来说是不意外的。先是家庭遭到红卫兵的洗劫和摧毁，然后是女主人公被捕入狱，狱外的女儿处境更糟。已经是上海电影制片厂著名演员的她，在与母亲失去联系的情况下，被人活活打死。事实上，这本书就是她劫后余生的母亲含泪写来献给她的。<br />
<br />
　　讲述个人和家庭的苦难在中国已经很难引起人们兴趣了。但是，人们绝不能因此而蔑视他人的苦难，或者压制他人讲述苦难的空间。这无异于另一种邪恶。而且，郑念的故事和她的讲述具有极大的魅力，深深吸引了我。<br />
<br />
　　这本书是用英文写成的，在西方世界引起了广泛反响。我正是在一本法国人写的上海史中得知这本书的。我读的是中文译本。应该说，翻译的味道很浓，英文所特有的那种冷静、严谨，或者说英国味，透过译文，依然清晰可辨。<br />
<br />
　　最早打破郑念平静生活的是两个上门通知她参加批判别人的群众会议的人。郑念表示不感兴趣。其中一个人愤怒地责问道：“难道你不知道无产阶级文化大革命开始了吗？”<br />
<br />
　　郑念的回答是1966年以后任何一个中国人都说不出来的：<div class="quote"><h5>引用:</h5><blockquote>“文化大革命与我们有什么关系？我们为商行服务，又不是文化组织。” </blockquote></div>　　1966年9月27日，经历了一个批斗会后，女主人公（笔名是郑念）被捕入狱。实际上，说入狱是不准确的，在未来的六年里，她一直被关押在看守所，甚至没有机会来到真正的监狱。因为坚持不认罪，她一直被拘留在看守所，在单人牢房里被拘留了六年。<br />
<br />
　　书中有大量郑念和其他人，如红卫兵、狱卒、审讯人员的对话。我相信，至少那些人当初不会说出书里的那些语言。那些语言太书面化、太清楚简洁，丝毫也不拖泥带水。可是，他们当时说的原话，必定用词简陋、语义含混、语焉不详。他们只有那种水平。可是，作者这样写，虽然失去了某种程度的真实，但这种笔法带来的阅读享受却是非常迷人的。对文革那种低贱荒谬的历史，我此前从没见过以如此高贵淳精的笔法进行过叙述。这种叙述本身就是对那种荒谬历史的最大蔑视 ――甚至在用词方式上，作者也绝不和他们同流。<br />
<br />
　　在作者对自身经历的叙述中，不时穿插有她对中国当时政治形势的分析和评论。这些分析和评论，在两个方面给我留下了深刻的印象。<br />
<br />
　　其一，古典人文教育确实是世界上最可宝贵的东西。接受过这种教育的人，即使没有专门研究过哲学、政治学、经济学等学科，也自然会有对人的权利、尊严的体验和珍视，有对自由和正义的向往，并有对邪恶势力的天然厌恶和警惕。他们中的杰出者，如郑念，还会因此而具有对抗邪恶的无尽勇气和坚定决心。<br />
<br />
　　其二，本书的中文译本内部出版于1988年。虽然是内部出版，译者和出版者对原书内容的大胆保留也是令人惊讶的。我不知道下列内容什么时候能再一次出现在大陆出版物中：<br />
<br />
&nbsp; &nbsp; 　<div class="quote"><h5>引用:</h5><blockquote>“再三邀请作坦率的建设性的批评，然后又严厉地处罚提出批评的人，毛泽东如此奸诈的行为完全吓坏了中国的知识分子，以至于中国的文化生活处于停滞的状态。”</blockquote></div>　　<div class="quote"><h5>引用:</h5><blockquote>“毛对知识分子的污辱达到了前所未有的残忍地步，这完全破坏了中国传统上对学术知识的尊敬。”</blockquote></div>　　<div class="quote"><h5>引用:</h5><blockquote>“我扪心自问，我曾看过如此众多的有关斯大林统治下的苏联的书籍，怎么会还看不透社会主义制度的本质。”</blockquote></div>　　<div class="quote"><h5>引用:</h5><blockquote>“多少年来，期盼着毛泽东的死，在我身陷囹圄时，我曾如此热烈地祷告，希望这愿望成为现实……这个人对我女儿的死有着不可推卸的责任，我期待着他将受到正义的评判，但这将会遥遥无期。”</blockquote></div>　　<div class="quote"><h5>引用:</h5><blockquote>“中国式的社会主义’实际上是中国共产主义的领导人为了保住面子而杜撰的。他们没有勇气公开承认社会主义中国已经失败。” </blockquote></div>　　正因为有对现实的深刻认识，郑念在六年的拘留生涯中始终拒绝认罪。指控他的罪名是里通外国、为英国人充当间谍。因为拒绝认罪，郑念曾经被背铐很多天，以至于双手落下了终身的伤害。但她选择坚持，用各种办法坚持。在智识和道德上，她压倒了审讯人员，以至于居然能得到某些局部胜利。在交代材料的底部，落款照例是“犯罪分子：”郑念每次都不厌其烦地在“犯罪分子”前面加上“没有犯过任何罪的”这几个字。在多次重写交代材料以后，再给她的纸上终于不再有“犯罪分子：”这个落款了。<br />
<br />
　　当然，这种略带喜剧色彩的事情绝不是六年拘留生活的主要内容。更多的是折磨、殴打、审讯、肮脏、饥饿、病痛、孤独、对亲人的苦苦思念和看不到希望。可是，在当局终于要释放郑念的时候，她却拒绝离开。<br />
<br />
　　林彪事件后，在中共内部大清洗的情况下，继续关押郑念这样的人对当局来说已经没有意义。郑念被带出来，有人向她宣布，将要对她宽大处理，释放出狱。郑念感到义愤填膺。她需要的不是宽恕，而是道歉。<div class="quote"><h5>引用:</h5><blockquote> “我内心深知，真正的罪犯……是我们都必须在此生存的罪恶的制度。我告诫自己，必须不惜一切代价进行斗争。” </blockquote></div>她拒绝了所谓的释放决议。她要求政府宣布她根本就是无罪，并且要求政府赔礼道歉，还要在上海北京的报纸上公开道歉。<br />
<br />
　　这种要求只能让专政人员感到好笑。他们当然永远也不会理解这种坚持背后的信念和价值观。<br />
<br />
　　郑念还是走出了看守所，因为她以为可以见到久别的女儿，可实际上，女儿在她失去自由不久就已经悲惨地死去了。<br />
<br />
　　以后的几年依然是文革的岁月。郑念被和另一家人一起安置在一座房子里。生活条件还算好于同时期的上海普通人。但从此以后，郑念似乎就要在和街道积极分子和片警的周旋中度过余生了。他信任的一个人居然是个告密者。“我什么时候才能过上正常的生活啊！”<br />
<br />
　　她去看望自己的一位老朋友，当年，他们两个年轻的母亲曾经一起在澳大利亚的海滩上漫步。现在，这位老朋友已经卧床不起，濒临死亡了。在告别的时候，老朋友给郑念留下的最后遗言是：“想办法出国吧，你还可以做到。”<br />
<br />
　　出国的机会终于在文革结束后出现了。文革结束以后，郑念成为统战对象，有机会出席各种妇女界的活动，并同时得到一些特权，比如在内部商店购物。郑念开始准备出国。<br />
<br />
　　出国的过程还是比较顺利的。郑念以看望在美国的妹妹的名义申请出国。一段时间以后，得到了批准，倒没有太多的周折。然后，为了得到美国签证，郑念找到渣打银行在上海的经理。他们是老相识。她可是银行的重要客户。见到郑念，这位经理有些吃惊，他以为郑念早已死去了。<br />
<br />
　　很快，郑念拿到了美国签证。从上海登船赴香港时，郑念在船上透过蒙蒙细雨，望着壳牌石油公司的大楼和她原来办公室的窗口，她知道，这一去，就将永远离开祖国。<br />
<br />
　　在香港，壳牌公司为过去的老员工安排好了飞往美国的头等舱机票。到了美国以后，她终于开始了自由自在、随心所欲的生活。<div class="quote"><h5>引用:</h5><blockquote>&nbsp; &nbsp;“在我新的生存空间，找不到像上海居民委员会卢英或户籍警老李这样的人物。在自由和轻松的氛围中，我感到精神焕发。” </blockquote></div>　　全书的结尾是郑念对中国文革后的政治经济形势的综合分析。这些分析，虽然写于八十年代，但现在读起来仍然很有启发性，是现在许多人所不及的。<br />
<br />
　　本书的版权页表明，此书印刷了5万册。看来，80年代的不少人读过这本书。我是后来者，21世纪才第一次读到这本书。据说谢晋当年曾经打算把这部书改编成电影。如果果然做到的话，应该比《芙蓉镇》的冲击力更强。当然，拍摄的难度也更大。<br />
<br />
　　80年代，读这本书的不少人感叹唏嘘。我想，这些人可能联想到了自己在疯狂岁月的遭遇，感同身受。我幸运地没有那可怕的经历，不至于“唏嘘”，但我另有一种强烈的感受，那就是对造成这一切悲剧的那个邪恶力量，从过去的痛恨变为极度的厌恶！简直就是厌恶透顶！<br />
<br />
　　我脑子里总是浮现一个并不存在的场景：一群肮脏的家伙闯入一个正在聚宴的大厅，把大桶大桶的粪便到处倾倒。客厅里的人固然目瞪口呆，恶心不已，可这群肮脏的人也没得到什么，他们只是更加肮脏，脏到臭不可闻。<br />
<br />
　　人们痛恨那些掠夺偷窃的强盗和贼，但这些肮脏的家伙，只能让人厌恶，多少高尚的人和美好的东西都毫无意义地毁在他们手里，毫无意义……<br />
