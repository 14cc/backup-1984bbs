													<h2>郑念女士去世</h2>
						
						
																											<div id="postmessage_318324" class="t_msgfont"><img src="http://i36.tinypic.com/2cwstfq.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
