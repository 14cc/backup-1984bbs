<br />
海派贵族赖以生存的经济基础，逐步在政治风浪中消失了。公私合营后的定，在十年动乱期间停止了。红卫兵的吼声打破了海派贵族最后的宁静，这十年，海派精英们，被打死、斗死、自杀的，不计其数。海派贵族迅速消亡，海派文化也在1960年代初最后绽放一下后，完全沉寂下去了。<br />
<br />
曾在抗日和历次内乱中顽强地生存下来的海派文化和海派贵族们，终究躲不过这一次灭亡的浩劫。<br />
<br />
等到1980年代开放，残留的海派贵族们缓缓醒来。对于他们来说，一切都已经过去。所欠的定息一次性发还了，自己的古董家具和房产部分归还了。可是这一切都无法挽回死去的亲朋、消除家庭离散之痛。他们与世界隔绝得太久了，昔日事业和社会的风光岁月已经不再。<br />
<br />
他们刚刚从伤痕累累中爬起来，第一件事情，就是选择让自己的子弟们离开中国。子弟们去了海外后，一部分老人独守在老宅、红木家具和回忆中度过晚年。另外一些老人也随着上一代人去了海外，终老异国。<br />
<br />
郑念女士在美国用英语所著的《上海生死劫》，成为了描述上海最后贵族们一段血泪经历的代表作。在国内，《傅雷家书》的出版和傅雷先生的遭遇，化作了上海最后贵族们的最后一声叹息。<br />
<br />
进入90年代后期，全中国突然爆发了一场寻旧热，特别是上海人，老上海、百乐门、汇丰银行、外滩、张爱玲等话题顿时火起来。模仿所谓的高尚生活方式，也被冠以娇滴滴的“小资情调”。红木家具、老克勒、小开等又时髦了起来。老上海话题的影视剧和小说，进入新一轮的火爆和炒作。<br />
<br />
对于娱乐圈和社会上对老上海的模仿秀以及众多模仿老上海的文艺作品，残留的海派贵族们，只能摇摇头，说一句“一群跳梁小丑，对于真正的海派贵族生活和品位一无所知”。<br />
<br />
青山依旧在，毕竟东流去。<br />
<br />
被毁坏的，不可能再被重复。中国新文化运动后所出现的、真正融会中西、推陈出新、具有勃勃生机的海派文化，最终随着海派贵族们一起消亡了。只留下一大批经典的作品、传奇故事、和几栋老楼，供后人凭吊。</div>

							
							
							
							
