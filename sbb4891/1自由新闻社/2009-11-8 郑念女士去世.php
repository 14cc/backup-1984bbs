《上海生与死》英文版封面<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 悼《上海生与死》作者<br />
&nbsp; &nbsp; 　以《上海生与死》一书闻名于英美文坛的作者郑念女士星期一（二日）病逝华府，享年九十四岁。郑女士在今年七月洗浴时不慎被热水烫伤而住院，伤势时好时坏，更不幸的是被细菌感染，终致不治。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　郑念本名姚念谖，原籍湖北，一九一五年生于北京，父亲是日本留学生，曾任北洋政府高官。郑念早年毕业于燕京大学，上世纪三十年代留学英国，就学于伦敦政经学院，师从著名的左翼政治学教授拉斯基（Harold J. Laski），和本报创办人余纪忠先生同学。余先生生前说，当时在伦敦政经学院的中国女留学生只有两人，郑念是漂亮的那位。郑念不仅漂亮，气质尤其好，一直到逝世前，始终保持美好的容颜和仪态。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　郑念后来嫁给同在英国留学的中国学生郑康琪，随夫姓易名为郑念。他们学成归国时，抗日战争已经爆发，双双奔往战时首都重庆，郑康琪加入了外交部，一九四一年外派澳洲，追随徐谟公使（战后曾任国际法院大法官）担任参事，驻澳长达七年，一九四八年十月才调回中国，出任外交部驻上海办事处主任，但大陆随即变色，郑因有老母，没有随政府迁台，「解放」后转职英商壳牌（Shell）石油公司担任中国总经理，但不幸于一九五七年因癌症病逝。郑念继其夫在上海任职壳牌石油公司，担任英国总经理的助理，直到文革前夕。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　文革期间，由于郑念是英国留学生，长期供职待遇优厚的外商公司，享受资本主义式的生活，被红卫兵抄了家，并被控为英国的间谍，系狱将近七年。这期间她在上海电影厂担任演员的独生女梅平遭红卫兵迫害致死。《上海生与死》即是写她个人的惨痛遭遇，因这是文革后第一本以英文写的亲身经历的书，一九八七年在英美两国出版后，造成轰动，使郑念一夕成名，并带来财富，请她演讲的邀约应接不暇，每讲一次可收入美金五千元，郑念晚年即靠书的版税和演讲收入生活，加上壳牌石油公司给她股票的红利，日子过得非常舒适悠闲，而最大的享受是她有了完全的自由─免于恐惧的自由。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　可是她心中也有一个永恒的痛，那就是女儿梅平的死。梅平生于澳洲，有澳大利亚国籍，根本不须回中国的，但一九四九年四月上海沦陷前夕，住在香港的郑念，奉先生之命把女儿带回大陆，因此铸成了毕生无可挽回的大错。这个错给她的伤痛，当她老病孤苦无依时，就越发加剧。而当她获悉杀害她女儿的凶手胡永年依然健在、儿孙绕膝时，更是心如刀割。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　当然还有无限的故国之思，可是三十年前她以探亲名义远适异域时，已发誓不再重返伤透她心的中国，而故国只能长存她的记忆中和梦里了。<br />
&nbsp; &nbsp; <br />
&nbsp; &nbsp; 　郑念之死，使我想起赛珍珠晚年的感慨。七十年代后期赛珍珠想重访中国，在遭受中共驻加拿大大使馆馆员的粗暴待遇后，相当伤心的表示：为什么共产党统治中国后，像胡适、林语堂那一代有文化、有知识、有教养的人都不见了？郑念可说是中国继胡、林之后有文化、有知识、有教养的最后一代，她的死，那个世代已成绝响。<br />
