																											<div id="postmessage_748856" class="t_msgfont">胡锦涛颁发命令状 章沁生等11人晋升上将军衔.<br />
星岛环球网 <a href="http://www.stnn.cc" target="_blank">www.stnn.cc</a> 2010-07-19 <br />
<br />
<br />
　　星岛环球网消息：中央军委19日在北京八一大楼隆重举行晋升上将军衔仪式。中央军委主席胡锦涛向晋升上将军衔的同志颁发命令状。<br />
<br />
　　中广网报道，下午15时30分，晋衔仪式在庄严的国歌声中开始。中央军委副主席郭伯雄宣读了7月5日由中央军委主席胡锦涛签署的晋升上将军衔命令。中央军委副主席徐才厚主持晋衔仪式。<br />
<br />
　　这次晋升上将军衔的高级军官是：副总参谋长章沁生、总政治部副主任兼中央军委纪律检查委员会书记童世平、总装备部科学技术委员会主任兼总装备部副部长李安东、军事科学院院长刘成军、国防大学校长王喜斌、北京军区司令员房峰辉、兰州军区司令员王国生、南京军区司令员赵克石、南京军区政治委员陈国令、广州军区政治委员张阳、成都军区司令员李世明。<br />
<br />
　　晋升上将军衔的11位高级军官军容严整、精神抖擞地列队主席台前。胡锦涛主席向他们颁发命令状，并与他们亲切握手，表示祝贺。佩戴了上将军衔肩章的11位高级军官向胡锦涛等领导同志敬礼，向参加仪式的全体同志敬礼，全场响起了热烈的掌声。<br />
<br />
　　中央军委委员梁光烈、陈炳德、李继耐、廖锡龙、常万全、吴胜利出席晋衔仪式。<br />
<br />
　　晋衔仪式在嘹亮的军歌声中结束后，胡锦涛等领导同志与晋升上将军衔的同志合影留念。<br />
<br />
　　出席晋衔仪式的还有解放军四总部、驻京各大单位和军委办公厅的领导等。<br />
