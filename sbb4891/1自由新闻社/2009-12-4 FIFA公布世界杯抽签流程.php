<br />
雅虎体育讯 北京时间12月4日17:00,国际足联发了有关世界杯抽签的最新消息,公布了本次抽签仪式的具体流程。根据国际足联的安排,抽签仪式将于南非时间晚上19点(北京时间1点)正式开始,届时全世界将有2亿名观众观看盛大的抽签仪式。&nbsp; &nbsp;<br />
<br />
FIFA在抽签仪式上,将邀请著名南非女影星塞隆,球星贝克汉姆,中长跑名将格布雷塞拉西,南非板球队的第一名黑人选手恩迪尼,南非橄榄球世界杯的选手施密特和布特作为抽签嘉宾。据FIFA的消息称,晚上的抽签仪式上,邀请了超过1700名各国媒体的代表来报道,邀请了2000名嘉宾,此外全世界将有2亿名观众观看。<br />
<br />
下午官方还要安排两次彩排,按照FIFA的安排,在当地时间18点,北京时间12月5日凌晨0点,将安排红地毯,32个参赛国将按照英文字母的顺序依次通过。其中意大利作为卫冕冠军,倒数第二个出席,而最后一个出场的是东道主南非。<br />
<br />
参赛国走红地毯结束后,将是世界杯组委会和国际足联执委会等相继通过,最后两个通过的人,将是南非总统祖马和国际足联主席布拉特。红地坛仪式结束,从19点开始,FIFA将安排很多文艺表演,其中主要包括(以下均为南非时间,19点是北京时间5日凌晨1点):<br />
<br />
19点00 抽签仪式开始<br />
<br />
19点01 约翰尼-克莱格表演,曲目《SCATTERLINGS OF AFRICA》<br />
