19点55 抽签正式开始 (长达28分钟)<br />
<br />
先抽第一档次种子队,南非自动进入A组,成为1号位。其他种子队意大利(卫冕冠军)、巴西、西班牙、德国、阿根廷、英格兰、荷兰随机抽出并自动本组1号位。<br />
<br />
再将第二档次的韩国、日本、朝鲜、澳大利亚、墨西哥、美国、洪都拉斯、新西兰随机抽出。<br />
<br />
接着把第三档次的喀麦隆、科特迪瓦、尼日利亚、加纳、阿尔及利亚、智利、乌拉圭、巴拉圭随机抽出。<br />
<br />
最后是将第四档次的法国、葡萄牙、丹麦、瑞士、塞尔维亚、斯洛伐克、希腊、斯洛文尼亚随机抽出。<br />
<br />
欧洲球队以外同洲不同组,第二档到第四档球队抽签分为两个步骤,首先抽所属小组,再确定小组位次(第2位到第4位)。其中第三档球队抽签时启动同洲回避程序。<br />
