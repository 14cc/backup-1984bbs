<br />
黑和白 <br />
<br />
　　雪球越滚越大。 <br />
<br />
　　重庆警方原先掌握的黑社会性质团伙有104个，但自“6.20”打黑除恶以来，涉案人数越来越多。“这刚揭开盖子，”一名警方内部人士说。 <br />
<br />
　　一位退休的重庆市公安局原副局长介绍：解放初期，鉴于重庆的陪都背景，公安在肃清异己分子上花了大力气，这里治安一直不错。但“文革”那番激烈的武斗后，重庆“乱套了”。小型的犯罪团伙开始崛起，但大多是诸如杀人、抢劫类的显性暴力犯罪，相对容易发现和缉拿。 <br />
<br />
　　“西部大开发必然会出现新一轮的人财物大流动，社会管理、控制和防范机制跟不上，社会治安随之也将出现新的情况、新的特点。”现重庆市政法委书记刘光磊曾表示。 <br />
<br />
　　进入21世纪――特别是近几年来，重庆的黑恶势力开始膨胀。一些黑社会性质团伙，如同这山城，错落地生长起来。他们开始划分地盘、明确分工、壮大队伍，并且开始渗透并逐步转向商业化。 <br />
<br />
　　“这世道，白道不如黑道来钱快。”老杨叹了口气说，这也是不少马仔趋之若鹜走黑道的原因之一。而在经济危机的影响下，重庆2008年的城镇登记失业率有所增长，越来越多找不到工作的人又给这个城市带来更多不确定因素。而近两年内发生的出租车罢运、“7字头”公交车事故，以及“3.19”枪案，更让主政者觉察到了问题的严重。 <br />
<br />
　　“（黑社会）渗透的领域不断拓宽，大到能源、交通、建筑等事关国计民生的重点项目，小到粮油菜肉等事关老百姓日常生活的商贸活动，只要有利可图，黑恶势力就无孔不入。”8月16日，重庆市市委常委、政法委书记、市打黑除恶专项斗争领导小组组长刘光磊接受采访时称。<br />
<br />
　　一位熟悉重庆黑道的商人对《中国新闻周刊》披露：重庆的涉黑势力主要集中在几个行业，首先是运输。 <br />
<br />
　　2009年年6月发生的民营公交“收编难”事件，就是某些利益集团对抗政令的案例之一――因民营公司经营的“7字头”公交车管理混乱，在不到3年时间内导致31人死亡20多人受伤。有重庆乘客甚至编出段子：“上7字头公交车必需物品：急救包一个，内装绷带、止血棉纱、夹板等；工具箱一个，内装剪刀一把，尖头锤一个，以备逃生。”重庆市政府终于决定在5月31日前，将全市380多辆“7字头”收归为国有。但有媒体披露：“民营公交公司提出的收购要价达到1亿多元，与政府谈判时的态度非常强硬”。 <br />
<br />
　　另一个行业则是“放水”（高利贷）。徐帆（化名）一身漆黑，寸头，黑T恤，黑色布鞋，还有墨镜，在室内也不肯摘下。他是重庆某追债公司的一员，平日找上三五个兄弟，穿上印着“讨债”字样的上衣，往债务公司一坐，直到对方还钱。他属于道上的“清水黑”――这个术语自袍哥时代延续下来，指的是那些不干下三滥的事情的道上人；另一种黑法叫浑水黑，就是没名没位，靠打杀的纯江湖人士。 <br />
<br />
　　在游动着数亿高利贷的重庆，讨债事业相当红火。甚至希尔顿酒店的围墙上，亦不乏“追债”的“牛皮癣”。许多讨债公司披上“代收代付货款”的外衣，进入商务公司的序列。 <br />
<br />
　　而在个别市场，有肉霸、菜霸，甚至可以进一步细分到，猪肉霸、牛肉霸、猪脆骨霸；在建筑行当，有沙霸、石霸、砖霸？？建立在威逼、恐吓甚至殴打上的垄断，已经成为某些行业的规则。 <br />
<br />
　　这些监管缺失的灰色地带里，犯罪技术和装备正逐步升级。一些团伙已经有了AK47，以及警方专用的移动电话定位系统。 <br />
<br />
　　“拜码头” <br />
<br />
　　不管“清水黑”还是“浑水黑”，“朋友”是不可或缺的财富。 <br />
<br />
　　自南宋以来，重庆商贸的码头历史绵延近千年，60％的居民都靠着码头吃饭，爬坡上坎、肩扛背挑。这是靠合作才能完成的工作，由此，比起一般的城市，重庆更具有抱团意识和人情味。 <br />
<br />
　　“常在河边走，哪能不湿鞋。”徐帆说。人在江湖，必不可少的两类朋友，一是律师，二是医生。干他们这一行，“进去”和肢体冲突再寻常不过。律师朋友像一条纽带，把见不得光的灰色群体和正大光明的公检法群体联系起来。 <br />
<br />
　　各种建立在金钱、亲情和友情之上的社会关系，在这座人口密度名列全国前茅的城市里，被紧密地织成一张网。 <br />
<br />
　　老杨每个月会缴800元“治安联防费”，他总把这叫作“保护费”，“门口卖菜的，摆小摊的也要交，算是拜了码头”。 <br />
<br />
　　上门收费的是有些“后台”的社会人员。不过他最在乎的是，拜过码头，洗脚房就不会被找茬了，严打的时候，会有人提前通风报信。一来二去的，他和“收保护费的”成为了朋友，有时还谈起孩子的学习问题。 <br />
<br />
　　一位重庆记者遭遇过一桩“怪事”：在他曝光重庆某派出所后不久，家里遭到盗窃。他前往派出所报案，但办案人员一见他就未卜先知一般，嘻嘻地问，家里出事了？后来这事便不了了之了。<br />
<br />
　　对于老杨的上级吴老大而言，“朋友”愈发地珍贵。以前经营休闲会所时，吴老大会把辖区内“数得上名号”的人统计在录，逢年过节就拜拜年、送送礼。 <br />
<br />
　　基于共同的利益，黑白两道彼此交融起来。道中人需要官方人物充当劫难时的保护伞；亦有公务员以认识黑社会为荣，认为这是“吃得开”的表现。而在更高一层――比如重庆市人大代表、亿万富翁黎强被警方带走后，他被扣留的手机不时有提示逃匿的短信声响起，发短信者不乏系统内部人士。<br />
<br />
　　那些做强做大的具有黑社会性质的犯罪团伙，已经拥有了一个合法化的企业外壳。里边往往汇聚了各色人士，比如打手、会计、消息灵通人士。有人专管敲诈恐吓，有人负责合法投资经营，把非法得来的钱“漂白”，相辅相成。 <br />
<br />
　　公安部新闻发言人武和平曾对黑社会犯罪进行研究，他将其分为初级、中级和高级三个阶段。<br />
<br />
　　初级阶段是显性的松散暴力团伙犯罪；而如今，重庆的黑社会犯罪已有升级迹象。 <br />
<br />
　　按照武和平的分析，中级阶段的黑社会犯罪，很多时候，是以有社会责任感的企业家、慈眉善目的慈善家这种身份出现。他们一般有自己的企业，组织严密，控制手段无所不用，确保自己获得利益，通过金钱、仕途和暴力手段，对能够与他们利益相关的部门领导威逼利诱，采取一种隐形的控制。 <br />
<br />
　　到了高级阶段，黑社会甚至可能推出自己的代理人，形成“影子政府”。所以，此次打黑对于这个直辖市的未来，就显得至关重要。 <br />
<br />
“不只是一个文强的问题” <br />
<br />
　　2008年6月，“打黑英雄”王立军空降重庆之前，在重庆的官方语境中，对城市治安的表述是，“根据统计，我市群众的安全感最近几年一直都保持在92％左右，2007年达到了最高峰的93.73％”。 <br />
<br />
　　但与此同时，重庆屡发涉黑、涉枪大案，整体治安形势严峻，这直接导致文强在当地民众中一直口碑不佳。根据重庆市公安局的统计，重庆近年来治安案件每年在10万件以上。 <br />
<br />
　　一位司法界人士认为，如此多积案，加上扫黑专项斗争所暴露的重庆黑社会势力猖獗的事实，“作为分管刑事侦查这么多年的公安局负责人，文强应负主要责任”。 <br />
<br />
　　2009年6月起，重庆下了狠心，一举端掉了14个大型黑社会犯罪团伙，8月初，任公安局副局长11年之久的文强因涉黑被双规。 <br />
<br />
　　在近50天的专项集中整治之前，重庆市委成立了“打黑除恶”专项斗争领导小组，政法、纪检监察、经济三条战线联手，令重庆的命案侦破率达到了91.35％，高于全国平均水平2.95个百分点。重庆官方还表示：破命案积案超过过去5年的总和，万人刑事案件发案率低于京、津、沪。 <br />
<br />
　　目前，已取得初步胜利的“打黑除恶”行动仍在继续。“这其中有我们欠社会的账，欠老百姓的感情账。”王立军对他的同事们说。而在网络上，许多人――包括一些外地人，也正在热烈呼吁“王青天”的降临。 <br />
<br />
　　萧条的是老杨和他的洗脚房，这次严打，好像漫长过任何一个冬天。不时有男子往里面探头望望，老杨不住地赔笑脸，“不好意思，停业整顿，改天来耍嘛。” <br />
<br />
　　但其实老杨自己也不晓得“改天”是哪天。他有种不好的预感。据说，这栋近20年的违章建筑，很快就要被拆除了。</div>

							
							
							
							
