重庆打黑大快人心，但看了今天一张新闻照片，感觉似乎完全不是这样。<br />
<br />
据报道，重庆市委书记薄熙来，在市公安局长王立军陪同下，近日视察璧山县城郊派出所和巡警大队。薄熙来强调，公安基层所队一定要加强警民关系建设，要让犯罪分子感到害怕。<br />
<br />
但新闻配发的照片却显示，薄书记等领导大步走在马路中央，颇有当年像美国大兵进入德军占领的城市的战胜者姿态。不难看出，摄影者用心良苦。大概过去重庆街头都被黑社会占领了吧。<br />
<br />
仔细一看，发现许多问题：<br />
<br />
一、严重违反交通法规，马路中央显然是机动车道，而非人行道，谁来了也改变不了这个事实。凭什么重庆市的领导就可以违法？驾驶员都知道压黄线属于严重违规啊，本人当年在西单还因此被扣过分罚过款呢。<br />
<br />
二、马路两边警察林立。说明什么？怕黑社会？怕老百姓？<br />
<br />
三、领导们这种违规行为让百姓看来会有什么想法？难怪黑社会这么多呢？原来领导都不懂法。<br />
<br />
四、新闻照片只见领导，不见百姓。想说明什么？都是领导的功劳？黑社会长这么大了才打谁之过？一个重庆几天就抓了６７个黑社会团伙？重庆还有多少黑社会组织没被抓获？全国还有多少黑社会组织没被抓获？去年奥运会前夕，各省的指标是“最少打掉一个黑社会组织”。重庆一夜之间完成了６７次奥运会的“指标”。是喜是忧？中国社会治安状况应该打几分？<br />
<br />
五、看了这张照片全国的黑社会都放心了－－原来领导都爱作秀。“让他们秀去吧，秀完了还是咱哥儿们的天下！”<br />
<br />
１９８３年中国严打运动就是从打击唐山黑社会开始的。黑社会越打越多，越打越大。说明了什么？<br />
<br />
重庆前公安局副局长、司法局长成为黑社会靠山说明了什么？薄西来打黑被迫从辽宁刁“打黑局长”来坐镇又说明了什么？<br />
<br />
什么是黑社会？白社会不作为才会有黑社会。黑社会之所以成为黑社会首先必须有黑白两道通吃、政府官员涉黑甚至领导黑社会。如果黑社会组织内部没有政府官员那就不叫“黑社会”，而叫匪徒或暴徒。<br />
<br />
无论如何，薄西来敢于硬碰硬、带头打黑还是值得大书特书的。既然如此，何不让薄西来们、王立军们负责全国的政法工作，顶替那些养虎成患的前辈们？<br />
<br />
重庆一地打出这么多黑社会，乌鲁木齐一夜之间就有上千暴徒行凶。这些都说明了治安形式的严重性。如果再不下狠手恐怕就来不及了。<br />
<br />
不过顺便提个醒，应当防止有人把所有事件都往黑社会身上推，动不动就说背后有黑社会云云。这次重庆打黑的目标之一也包括所谓煽动他人上访的黑社会。镇压这种跟帮老百姓申冤的“黑社会”还是谨慎点好。如果这种“黑社会”的行为谨限于如此的话，恐怕不能叫“黑社会”。那样的话，百姓连上访的胆子都没有了。警匪一家的“黑社会”更没人敢管了。<br />
