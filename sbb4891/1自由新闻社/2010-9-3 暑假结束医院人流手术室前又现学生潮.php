<br />
　　她们中有的才上初二，有的大学没毕业已做了两次手术<br />
<br />
　　她们说故事，都带着哭腔<br />
<br />
　　医生你能看出来，孩子是啥时候有的吗？<br />
<br />
　　暑假结束，各大医院做人流的学生潮又开始涌来，她们的面容尚显稚嫩，态度桀骜不驯……在医生眼里她们都还只是孩子。来做人流的学生中，有大学生、高中生、初中生，但你很难走进她们的世界，偶尔有几个女孩肯站出来，带着哭腔讲述了她们的故事，而大多数女孩都骂着粗口离开了。<br />
<br />
　　从8月24日到27日，本报记者在昆明某知名民营医院早孕门诊连续蹲守4天，采访这些令人揪心的故事。<br />
<br />
　　24日上午9点―10点40分<br />
<br />
　　问诊学生人数：4人<br />
<br />
　　阿芬<br />
<br />
　　做完手术<br />
<br />
　　才觉得自己傻得很<br />
<br />
　　阿芬20岁，身体壮实，高考之后她放松下来，她长了很多肉。“原来才53公斤，现在已经是60多公斤了。”阿芬今年考上了省外的专科学校，还没开学，阿芬突然开始呕吐，而且停经。阿芬怀疑自己怀孕了，用验孕棒一验，没错！<br />
<br />
　　阿芬向父母撒谎，说要出去玩，一大早就赶到医院。上午9点，阿芬成为诊室内的第一个患者。阿芬羞羞答答，一顶橙色的棒球帽几乎遮住了她的整个面孔。“医生，我怀孕了。”阿芬声音小得出奇。“啥？”医生问道。阿芬开始大哭起来，“我怀孕了。”“结婚了吗？”“没有。”“多大了？”“19。哦，不对，20。”阿芬和医生一问一答。“什么时候发现的？”“前天早上。”医生头都没抬，在一张处方单上写着病情。阿芬的眼泪直直地掉下来，打在她的牛仔裤上。<br />
<br />
　　医生给她开了相关的检查项目，阿芬孤身一人走进各个检查室。拿到检查结果的时候，阿芬再次回到诊室。10点20分，医生看着检查结果问：“这个孩子要吗？”“不要！不要……”阿芬还在哽咽。手术单开好，阿芬还不想离开。医生说：“去走廊尽头的人流手术室排队吧。”阿芬坐着不动，像是害怕，又像是还有话要说。<br />
<br />
　　阿芬从椅子上站起来。“医生，能和你单独说一句话吗？”医生移动到检查室的幕帘旁边，阿芬压低声音说，“医生你能看出来，那个是啥时候有的吗？”阿芬的头低得更低了，“我7月和8月各有一个男朋友。”医生无言，简简单单地说了一句“看不出来”，就回到了座位上。<br />
<br />
　　人流手术室门外，阿芬被捂着肚子、表情痛苦的患者们吓着了。“会不会很疼啊？”轮到她的时候，她小声地问医生。医生说：“不疼。”医生的桌上放着一瓶乳白色的麻醉剂叫做“小牛奶”，在手术过程中，它能让阿芬熟睡。<br />
<br />
　　阿芬高考后等成绩的时候和前男朋友分手，在填完志愿后又和新男朋友去外面旅游。阿芬才20岁，她却觉得上了大学自己已经是个女人了。做完手术后，阿芬不这样想了。她说自己其实还是个孩子，傻得很。<br />
<br />
　　25日中午12点―下午2点30分<br />
<br />
　　问诊学生人数：5人<br />
<br />
　　李婧<br />
<br />
　　初二学生<br />
<br />
　　卖掉电单车来做人流<br />
<br />
　　粉红的裤袜、蓝色的热裤、hello kitty的宽T恤、短短的头发上别着一个花格子的蝴蝶结……李婧站在诊室外时，很多好奇的目光在他身上不停地打量。<br />
<br />
　　李婧才14岁，她是逃学来做人流手术的。她把自己的电动车卖了一千块钱，才能站在这里。李婧是某中学初二的学生，马上升初三，学校的补课已经开始一周左右了。<br />
<br />
　　即使有很多人不停打量着她，还有人指指点点，但李婧还是不停地玩着手机链，昂着头，一副毫不在意的表情。<br />
<br />
　　今年暑假，李婧参加了一个英语夏令营。夏令营从昆明到上海、广州、香港，然后回到昆明。团员当中，也有一些高年级的男生，老师也是一些大学英语专业的。<br />
<br />
　　花季少女李婧在夏令营里很受瞩目，她很快对一个高二的男生产生了好感。“他很好看的，长得非常帅。”李婧咬着下嘴唇说，一来二去，两人越轨了。回来不久，李婧开始补课。有一次她竟然在操场上玩耍时晕倒，她谎称自己没吃早点。骗过了同学，她清楚地知道，自己怀孕了。“电视剧里都是这么演的啊，谁不知道啊！”<br />
<br />
　　12点30分，李婧乘着中午休息就跑出学校来到医院。人流室门外，李婧还是一副桀骜不驯的样子。“为什么不给那男的打电话啊？”李婧轻蔑地笑了笑，“你也太幼稚了，给他打电话能起啥作用啊？我知道我和他不可能，就是随便玩玩嘛。”李婧的话说得轻松极了，她已经想好了电单车的事情，“给家里说被偷了，他们还能把我怎么样呢？”李婧轻蔑地笑着。<br />
<br />
　　下午1点20分，李婧睡着了。她稚嫩的面孔上看不到任何痛苦，头上的蝴蝶结歪歪地挂在头上。1点40分，李婧醒来，腹部略微的疼痛让她难受。“X，不是说不疼的吗？”李婧皱着眉头。<br />
<br />
　　点滴还有半瓶，她叫医生拔了针头，捂着肚子走了。下午2点开始上课，她必须赶回去。李婧在医院门口拦了一张出租车，“谢谢你。回头电话联系啊！”说完，她微笑着给记者留了个只有10位数的电话号码。<br />
<br />
　　26日上午8点30分―10点40分<br />
<br />
　　问诊学生人数：7人<br />
<br />
　　小花<br />
<br />
　　大二学生<br />
<br />
　　第二次做手术<br />
<br />
　　今年9月开学后，小花才上大二，但她怀孕已经不是第二次了。小花轻车熟路地来到医院，在治疗感染的病房门外，她还给其他人解释：“如果先不把感染治好的话，就会引起其他的疾病。”小花还不止一次地安慰着同来的人：“没事的，几分钟的事情嘛。”<br />
<br />
　　今年暑假，小花回到山东老家后，碰上了高中时的男朋友。对感情生活没有禁忌的小花和男朋友发生了关系。在小花的大学里她还有一个男朋友，暑假快结束的时候，小花找了借口提前返回云南，其实她怀孕了。<br />
<br />
　　“我就搞不明白，我们有安全措施啊？”小花一脸疑惑，她是很多女生中第一个大大方方接受采访的。小花高中时跟学校篮球队的队长谈恋爱，后来因为分隔两地上学，两人分手了。一年后再见，小花和前男友又好上了。<br />
<br />
