																											<div id="postmessage_248303" class="t_msgfont">叶大鹰：好的片子多放两场吧 《天安门》被放到单厅放映 时间段很尴尬 导演抱怨 <br />
<br />
来源：法制晚报 2009年9月18日<br />
<br />
&nbsp; &nbsp; “我不是在埋怨，我是在恳请影院经理，帮我多放两场……”今晨电话那头的叶大鹰，声音沙哑、语气低沉。<br />
&nbsp; &nbsp; 9月3日，国产献礼大片《天安门》在国内上映，票房惨淡。上映几天之后北京各大影院就减少了排映场次。近日，该片导演叶大鹰在其个人博客上发表文章，对影院的排片安排表示强烈不满，他抱怨：“只要看过《天安门》的观众都说好，为啥落到今天这个地步？”<br />
&nbsp; &nbsp; 在博客中，他恳请各地院线的经理们：“把 《天安门》多放几场，让观众去看看毛主席吧！”<br />
&nbsp; &nbsp; 为此，本报采访了叶大鹰导演和北京两家豪华影院的负责人，一探究竟。<br />
<br />
叶大鹰抱怨・放映时间<br />
&nbsp; &nbsp; 在博客中，叶大鹰表示，《天安门》在很多影院被放到单厅放映，放映时间大多都是些尴尬时段。有些影院两天才放一场，甚至不再放映。<br />
<br />
院线回应<br />
不是不排是确实没人看<br />
<br />
&nbsp; &nbsp; 北京星美影院发展有限公司总经理袁鑫今晨在接受记者采访时透露：“影片上映第一天，我们排了7场，但观众一共才100多人，平均下来每场十几个人，这确实是没办法。”<br />
&nbsp; &nbsp; UME华星国际影城的总经理助理刘晖也说：“上映第一天我们也是排了7场，确实没多少人看，票房一共才2000多元，第二天就改为3场了。”<br />
<br />
叶大鹰抱怨・宣传规模<br />
没宣传观众不知道<br />
<br />
&nbsp; &nbsp; “片子好有什么用？基本上没做什么宣传，观众根本都不知道。”今晨，当记者打通叶大鹰的电话时，他沙哑着声音，“有人说我是急得上火了，气得吐血了。”<br />
&nbsp; &nbsp; 《天安门》和《建国大业》一样，都是由中国电影集团投资。叶大鹰直言不讳地表示：“最近他们几乎把所有的精力都放在了宣传《建国大业》上，我们是没人疼的孩子。”<br />
&nbsp; &nbsp; 叶大鹰说：“如果是片子烂，那我真是没话说，关键是我这个片子好，至少只要看过的观众都说好，为什么不好好宣传一下呢？”<br />
&nbsp; &nbsp; 叶大鹰表示：“《建国大业》的宣传规模至少是《天安门》的20倍，你说我们有得比吗？”<br />
<br />
院线回应<br />
营销和宣传上都有问题<br />
<br />
&nbsp; &nbsp; 对于观众为何不买账，两位受访者则表达了不同的看法。袁鑫说：“还是营销手法上有些问题，比如就说《天安门》这个片名，它基本上就没什么商业含量，很难吸引年轻观众入场。”<br />
&nbsp; &nbsp; 刘晖则认为确实是宣传的问题：“片子是好啊，但是观众不知道，如果单靠口碑，它有时间差，影院耗不起这个时间，等观众知道它好时，影片也差不多下线了。”<br />
记者手记<br />
&nbsp; &nbsp; 与《天安门》同期上映的《气喘吁吁》，被网友戏称为“史上最烂影片！”负面评价铺天盖地，就连主演葛优本人也出来公开指责，但还是不断有人掏钱进影院，难道观众傻了？<br />
&nbsp; &nbsp; 所以，一部电影有没有明星很重要、有没有话题也很重要。<br />
&nbsp; &nbsp; 影院是盯着观众的钱包安排场次的，如果片子没人看，拍得再好，也只能是孤芳自赏了。<br />
<br />
