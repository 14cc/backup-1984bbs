<br />
　　钢铁业结构调整，是温家宝一直在思考的问题。一个多月来，他已连续多次到钢铁企业考察，指出必须淘汰落后产能、加快兼并重组。在太钢考察时，他说，要把钢铁业的发展与信息化结合起来，与新材料发展结合起来，与循环经济结合起来，使钢铁业经过国际金融危机的锤炼真正强大起来。<br />
<br />
　　太原重型机械集团，是新中国自行设计和建造的第一座重型机械厂。近年来加快结构调整，培育了一系列名牌产品，企业在国际金融危机影响下销售收入仍然快速增长。当听说最新研制大容量挖掘机市场热销时，温家宝说，挖掘机、起重机看似笨重，但大并不等于粗，一定要精益求精，生产出高端产品，就会永远立于不败之地。他指出，应对金融危机，必须推进结构调整。要把扩大内需，特别是消费需求作为一个长期的、根本的方针。经济发展与结构调整是相互促进、并行不悖的。只有经济增长了，结构调整才有动力和支撑；只有调整结构，经济才能稳定、健康和可持续发展。<br />
<br />
　　靠新兴产业培育新的增长点<br />
<br />
　　如何加强科技支撑，培育新兴产业，是温家宝总理这次调研的重点。4日下午，他走进了富士康（太原）科技工业园。<br />
<br />
　　公司展厅里，摆放着一件件新颖别致的铝镁合金构件，一个个精巧细致的塑胶、冲压模具，展示着最新研制成果。从金属材料实验室，到冲击实验室，温家宝逐个认真察看。在显微镜下，在电脑检测仪器旁，他还亲自操作检测金属成分，并同技术人员交流。<br />
<br />
　　随后，温家宝又来到模具生产车间，十多位年轻女工正在埋头认真打磨铝合金模具。企业负责人赶忙告诉总理，这些女工是聋哑人，但个个都是技术能手。总理竖起大拇指用手语称赞她们自强不息，心灵手巧。<br />
<br />
　　随着技术进步，煤矿采掘设备正成为一个充满希望的新兴产业。温家宝来到山西煤机装备有限公司。这是一家与煤炭科学研究院相结合的科技型企业。公司新研制出搬运采矿设备的支架搬运机，替代进口，价格便宜，市场畅销。温家宝看后勉励工人们，要始终围绕“安全、可靠、质量、效益”八个字，将高新技术融入生产中去。<br />
<br />
　　在考察中，温家宝多次强调，谁在科技上占领制高点、谁掌握了关键技术、谁具有自主的知识产权、谁的高端产品多，谁就能在竞争中长期占有优势。当前，尤其要重视依靠科技发展新能源、新材料、生物制药、节能环保等新兴产业，培育新的经济增长点。只有这样，才能在市场竞争中站得住脚，保持领先地位和持续竞争力。同时，要适应市场需求，防止一哄而起，造成新的产能过剩。<br />
<br />
　　发展经济的根本目的是改善民生<br />
<br />
　　5日上午，温家宝来到大同，专程看望大同煤矿工人。这是总理多年的一个心愿。<br />
<br />
　　离大同市区10多公里，一座新的卫星城正在兴建。这就是大同采煤沉陷区和棚户区改造治理工程。在党中央、国务院的关心下，从2006年4月起，这项改善矿工居住条件的工程启动，规划建设规模达到815万平方米。<br />
<br />
　　总理的到来，让小区沸腾起来，大家纷纷走出家门夹道欢迎。走进焦建忠家，这位今年已经65岁、曾经在井下采煤40年的老人非常激动地对总理说，他原来住在低矮破旧的棚户区里，不避雨不挡风。三年前搬进新家。搬来的当天晚上，他和老伴激动得一夜都没合眼。说起往事老人眼角湿润了。总理说，尽管已经过去三年了，但还要向你们祝贺乔迁之喜，你们的住房条件终于改善了，我和你们一样高兴。<br />
<br />
　　走出焦建忠家，矿工和家属们拥了过来。温家宝深情地对大家说，煤矿工人为中国煤炭产业、中国工业发展做出了重要贡献，党和政府不会忘记，人民不会忘记，历史不会忘记。你们应该过上幸福的生活。我们还会逐步改善工人的生活，完善社会保障，使大家的生活一天比一天好，一年比一年有盼头。<br />
<br />
　　大同煤矿集团塔山矿井年产量1500万吨，现有职工800人，人均效率已经跨入国内一流水平。这个矿井还是全球最大的单井口矿井，在地下开挖的单井口矿井中产量最高。<br />
<br />
　　“我要到井下看看矿工们心里才踏实。”温家宝来到塔山矿井时对大家说。他换上工作服，穿上雨靴，戴上矿灯，乘坐井下专用车，通过7公里长的巷道，来到460多米深的矿井采掘区。温家宝走到矿工们中间，紧紧握着他们的手一一问好，矿工们顿时兴奋起来。<br />
<br />
　　在液压立柱撑起的作业面，一位工人正在操作采煤机采煤。隆隆机器声里，采掘的煤炭通过传送带运到井外。温家宝站在采煤机旁，仔细地观察了采煤的整个过程，并不时询问煤矿安全生产的情况。<br />
<br />
　　临近中午12时，从早上7时就下到矿井里工作的工人们要吃中饭了。在巷道里，温家宝坐在枕木上与工人们一起用餐。他一边吃饭，一边询问工人们的生产生活情况。他对大家说，中国是世界上最大的产煤国，应该有世界上最先进的矿井、最先进的技术和设备、最优秀的产业工人。在应对国际金融危机时，一定要把煤炭生产搞好。必须正确处理保增长与安全生产的关系，任何时候安全生产都是第一位的，在安全生产中实现经济平稳较快增长。同时，要下决心引进新技术，大力推进清洁生产，让煤炭生产质量和效益走在世界的前列。温家宝在矿井下呆了两个小时，才与矿工们依依惜别。<br />
<br />
　　4日晚，温家宝在太原主持召开了企业家座谈会，听取大家的意见建议，并作了重要讲话。<br />
<br />
　　在山西期间，他还到太原市和平街道办事处康乐社区看望广大居民，考察了汾河治理太原市南内环段工程。</div>

							
							
							
							
