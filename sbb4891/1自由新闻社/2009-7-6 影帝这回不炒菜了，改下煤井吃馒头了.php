　　新华网太原7月5日电&nbsp;&nbsp;中共中央政治局常委、国务院总理温家宝近日在山西考察时指出，巩固和发展当前经济企稳回升的好形势，必须统一认识，坚定信心，狠抓落实，保持宏观经济政策的连续性和稳定性，推进结构调整，培育新的经济增长点，提高经济增长质量和效益，保障和改善民生，实现经济平稳较快发展。<br />
<br />
　　4日至5日，温家宝先后来到太原、大同，他进工厂、下矿井，就经济社会发展和群众生活问题进行调查研究。<br />
<br />
　　调整结构才能立于不败之地<br />
<br />
