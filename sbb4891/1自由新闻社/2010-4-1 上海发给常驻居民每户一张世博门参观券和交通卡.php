<br />
　　一是票务引导，通过售票方式引导。目前的售票方式分各地发售的个人票、团体票和世博局发售的个人票。通过旅行社发售团体票，参观是需要预约的。这样的票务结构，我们可以想到卖出的团体票越多，游客的均衡性就越好。世博局发售的个人票也能进行适当控制，如果5月份游客集中，就可以少卖点，通过自营票销售来控制调节售票的数量和节奏，调节游客参观的时间和速度。<br />
<br />
