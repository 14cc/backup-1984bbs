													<h2>上海发给常驻居民每户一张世博门参观券和交通卡</h2>
						
						
																											<div id="postmessage_542869" class="t_msgfont">为了表示对上海市民这种奉献精神的感谢，世博会试运行期间，上海将组织所有的搬迁走的居民参观世博会；正式运行期间，还将给所有的常住户口的居民每户免费赠送一张世博会的参观券和一张价值200元的交通纪念卡。<br />
