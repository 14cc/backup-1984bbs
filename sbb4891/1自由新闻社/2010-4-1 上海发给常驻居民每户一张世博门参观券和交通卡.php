竭尽全力忠于职守办好世博――俞正声接受媒 体采访<br />
2010年4月1日 05:52&nbsp;&nbsp;来源:东方网&nbsp; &nbsp; 选稿:王呈恺<br />
<br />
<a href="http://2010.eastday.com/G/20100401/u1a715034.html" target="_blank">http://2010.eastday.com/G/20100401/u1a715034.html</a><br />
<br />
在中国2010年上海世博会倒计时30天之际，中共中央政治局委员、上海世博会执委会主任、上海市委书记俞正声接受了中央和上海主要媒体的联合采访。俞正声表示，我们衷心感谢全国人民对世博筹办工作的大力支持。我们会竭尽全力，忠于职守，办好世博。<br />
<br />
　　园区工程建设都将按时完成，我们将以一个真实的上海迎接世博会<br />
<br />
　　问：在世博会倒计时临近100天之际，胡锦涛总书记来上海考察世博会筹办工作时提出了“三个展示”、“六个确保”的要求。现在，世博会正进入倒计时30天，上海各方面筹办工作总体推进情况如何？<br />
<br />
　　我们对参展者、参观者服务意识怎么样，这个本身是无形的展示，也是非常重要的展示<br />
<br />
　　俞正声（以下简称俞）：当前，世博会正处于从筹办向举办阶段转换的关键时期。在党中央、国务院的高度重视下，在中央各单位、各省区市以及各新闻媒体的帮助和支持下，世博筹办工作按照中央要求，加紧做好准备，进入临战状态。各项基础设施正按照时间节点有序推进，能保证按期完成；世博安保和园区运行的制度性规定也已经基本确定，有些已正式发布，有些陆续要发布；进一步理顺了世博会运行阶段的指挥体系，成立了安保、主运行、外事工作、接待服务、宣传及媒体服务等五个指挥部，各种组织架构从市里到区县、从园区外到园区内都已到位；宣传、接待、交通等各方面的服务保障措施也都明确了。全市上下正全力以赴、精益求精做好最后冲刺阶段各项工作，大家都对举办一届成功、精彩、难忘的世博会充满了期望和信心。上海世博会，要展示中华民族5000年灿烂的文明，展示新中国60年特别是改革开放30多年的辉煌成就，展示我国各族人民为实现全面建设小康社会目标而团结奋斗的精神风貌。从园区本身来看，这主要是依靠中国国家馆、各省区市馆和一些企业馆来体现。这些馆会很精彩，从中可以看到我们国家的变化和对未来的期望与设想。此外，做好“三个展示”，还要依靠我们大家，靠我们园区团队的服务精神来体现，靠我们这座城市的服务意识，对参观者、参展者的服务来体现。我们提出三个服务：服务参观者、服务参展者和服务市民。我们对参展者、参观者服务意识怎么样，这个本身是无形的展示，也是非常重要的展示。<br />
<br />
　　“六个确保”，就是要确保场馆设施建设和布展工作如期完成，确保运营服务保障全面到位，确保安保工作万无一失，确保外事工作落实到位，确保新闻宣传有声有色，确保社会氛围文明祥和。园区的进度，大家都看到了。很多同志担心在园区建设上会不会来不及。在这方面我没有催过园区建设者。我知道因为各种原因，我们的开工时间是相对晚了点，但我对上海的建设队伍素质是有信心的，我们在硬件建设上不会有大的问题，实践证明也没有大的问题。<br />
<br />
　　我曾讲过，确保世博安全，是摆在我们面前的一场硬仗，是世博会成功与否的关键。现在，安保方面大的制度和规范都定了。要防患于未然，确保安保工作万无一失，关键是抓社会面发动，把群众发动好。上海的社区管理一直以来都做得很好，这次举办世博会，面对这么大的人流量，我们的社区工作要更进一步深入，做好对流动人口情况的了解，做好住宅小区的安全防范，做好对社会情况的掌握，做好社会矛盾的调解，帮助困难群众解决好问题，等等，这都是做好基层基础工作的重要内容。我们已经做了很多部署，很多区落实得很好，很多街道、居委会的同志很辛苦，工作很认真。我对街道、居委会在接下来的六七个月时间的繁重工作中，把基层工作做好是有信心的。<br />
<br />
　　问：世博开幕在即，园区内外工程建设和配套工程的进展状况到底如何，能否如期完工，上海能否以完美的面貌迎接海内外宾客，大家比较关心。<br />
<br />
　　真实的上海有她前进的一面，有她好的一面，也存在困难和问题。要解决这些问题，我们还要付出长期的很大的努力<br />
<br />
　　俞：我还没有听说世博工程有在开园前不能完成的，没有一个单位报告过不能按期完工。从硬件建设上说没问题。如果说有点问题，那就是可能会有极个别参展方因为各种原因，布展进度慢点，不能按预期开展。这也是历届世博会都有的情况，并不会影响世博会的大局。上海的配套基础设施建设主要是轨道交通，按计划完成也没有问题。虹桥枢纽也正式启用了。<br />
<br />
　　至于要说上海还有没有哪儿脏、破，肯定有。一个陆家嘴，代表不了整个上海，真实的上海也不只是世博园区，真实的上海是上海的全体。上海有的地方还有很多破旧的房子，还有不少居民生活很困难，这是上海的真实情况。要解决这些问题，我们还要付出长期的很大的努力。也就是说，我们迎接世博会，不是用一个十全十美的上海来迎接，这是不可能的，那不是真实的上海。真实的上海有她前进的一面，有她好的一面，也存在困难和问题。<br />
<br />
　　最关注的问题是园区参观秩序，最费脑筋的是客流的平衡。我们将通过五方面引导措施尽力均衡客流<br />
<br />
　　问：世博会即将开幕，在世博会筹办方面，在剩下的三十多天里，还有哪些重点工作？您最关心的问题有哪些？最困扰您的有哪些？<br />
<br />
　　六场试运行主要目的是检验我们各个环节存在什么不足，然后改进我们的工作<br />
<br />
　　俞：目前，我们的安保框架、包括园区运行的一些制度性的主要措施都定了；有关交通的一些措施办法也定了；园区的建设、一些基础设施的建设都会按时间节点完成。我们当前最关键的是抓各项工作的落实，在抓落实中不断发现问题、改进工作。这一方面要靠检查，一方面要靠试运行期间的六场演练。我们准备组织的六场试运行，主要目的是检验我们各个环节存在什么不足：入园环节有什么不足，展览排队啊、预约啊，这些方面总要运行一下，看看有什么毛病。我们原来设想的很多制度反应和处置对不对。要通过暴露问题发现问题，然后改进我们的工作。另一个方面呢，要安排一些人，比如园区建设者、安保工作者、园区工作者、动拆迁居民先来看一下我们的世博园，向他们表达我们的谢意。<br />
<br />
　　过去我讲过最关注安全和秩序，安全方面我们公布了一系列措施。记者朋友们也在报道中透露了一些，比如园区安检措施，在世博会的门票上写着：要按照规定安检。世博局也将公布相应入园的安检措施，禁止带瓶装水、刀具、运动设备、航空模型和飞行器物等。到正式公布时，大家会看到准确的文件。我们城市中一些确保安全的措施，也要以规章的形式正式公布。<br />
<br />
　　当前我们最关注的还是园区秩序问题。7000万人次在184天里出入园区，平均每天要达到40万人次。3.28平方公里，大家可以算出来每平方米有多少人。而且184天不是均衡的，刮台风人少，双休日人多。为了避免人多拥挤，我们要尽可能采取措施，使人流均衡些。<br />
<br />
　　当然最容易想到的简单办法，就是每张票写上固定日子，每天卖多少票那就有多少人看。但世博会不是体育比赛，不是看电影，对于参观者限制太多会妨碍参观者的方便，客观上讲，会有很多游客买了票暂时去不了，或想和朋友一起去。按天卖票，在历届世博会历史上也都没有采取过。我们要把世博会园区的秩序和参观者的方便结合起来，不能单纯考虑园区秩序问题。所以发售门票没有采用按天发售的办法。我们目前发售的一种叫“指定日票”：即规定日期，只能这天来，不是这天的票就不能入园,比如5月1日、2日、3日，10月1日至7日，10月的最后一周。这些天的游客会很多，所以发售门票要有所限制。还有一种叫“非指定日票”：即除了以上的日子，平时都可以入园。“指定日票”是历届世博会所没有的，我们按照实际情况设计了这种票，这个办法能否完全解决人流均衡的问题？我想不能，这只能解决指定日的人流问题。<br />
<br />
　　在门票的销售上还有什么办法？有人考虑设计“周票”，但这样会把人流集中到双休日，也不行。我们想了很多办法，但想来想去都不理想，没有更好的办法。那么，目前我们还采取什么办法来平衡人流呢？<br />
<br />
