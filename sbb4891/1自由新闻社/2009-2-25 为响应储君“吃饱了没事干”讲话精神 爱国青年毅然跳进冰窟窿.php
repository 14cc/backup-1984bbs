<br />
然后我就想申请一个欧洲的冬泳记录（奥地利这地方我真是无语了，我跑到维也纳国家体育局，竟然没有这个记录，体育局的人也不知道如何申请，我问了很多记者，他们也不知道如何申请）。我曾经去过大使馆求助，他们对我答复是，请去大使馆的网站去查我们的大使馆的原则，请参考党章，一个官员还说，中国人体质本来就不如老外，当时我就很无语了。<br />
