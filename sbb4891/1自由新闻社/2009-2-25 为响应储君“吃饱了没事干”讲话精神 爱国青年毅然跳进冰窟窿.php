<strong>我没给中国人丢人：气温零下7度我在奥地利湖中冬泳2.7公里</strong><br />
<br />
我没给中国人，河南人丢人，在气温零下7度，水温4度的湖里冬泳2.7公里，坚持了80分钟！<br />
<br />
我是河南郑州人，在奥地利上学工作。<br />
<br />
2008年的12月28日，那天零下十度我在湖里游泳，当时仅仅是很单纯的游泳，游到湖对岸正想休息一段时间然后游回去，然后对面的老外就报警了（新闻上写的是中国人，其实是老外报的警），当时我在一个小屋里休息本来打算一会游回去的，然后听到外面防空警报大声响起来，当时我没当回事，后来听到外面有直升飞机，快艇的声音，然后我就估计是找我的（因为WOERTHERSEE是旅游区，冬天岸边几乎没人住），快艇和直升飞机看见我，都向我开过来，当时我也有点蒙，后来就上了快艇回到了对岸，然后到岸上一堆记者拿个话筒向我捣来，当时我就说因为我是个中国人，然后就被一堆医生送到医院检查去了，那医生抽了我都快一斤的血，检查了一个小时，然后说没事，我就走路回家喝啤酒去了 。<br />
<br />
（期间一堆记者在门口要拍照，我让几个小护士把他们打发走了，因为我不想让他们看见我在医院的场景，虽然我一点事没有，但是明天他们就会有的写了----一个中国人零下10度去游泳然后被送到救护车然后在医院快死了，然后中国人体质很差等等（在这里我劝大家不要太相信西方媒体，他们不知道的事情就会胡乱编造，这次我的体会很深，比如这次政府都给我写信了不用让我付钱，但是媒体还写着这个中国人要付钱等等） <br />
