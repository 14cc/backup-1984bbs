																											<div id="postmessage_630641" class="t_msgfont">奥巴马签署新闻自由法 称压制言论国家将受惩罚<br />
2010年05月21日 16:52东方网<br />
中国专家：美国要抢占道德制高点 奥巴马签署新闻自由法<br />
<br />
本报特约主笔 崔向升<br />
<br />
据英国广播公司17日报道，美国总统奥巴马签署了《丹尼尔・珀尔新闻出版自由法案》，使之成为法律。根据该法，美国国务院的年度人权报告将列举那些侵犯新闻自由国家的名单。奥巴马称，此举传递给其他国家一个强烈的信号：美国对各国如何对待媒体很重视。那么，美国此举意味着什么？<br />
<br />
“压制言论”的国家将受惩<br />
<br />
奥巴马称，该法将表明，那些压制言论自由的国家，将会因为他们对媒体的行动而受惩罚。奥巴马声称，丹尼尔・珀尔离开人世的那一刻，牵动整个世界，它提醒我们，媒体自由是多么珍贵。“这个法律以庄严的方式，使我们明确地站在新闻自由这一边。”<br />
<br />
新法授权美国国务院确认哪些国家违反了新闻出版自由原则，认定这些国家的政府是否参与、提供便利或容忍这些侵犯行为，对这些国家所采取的保护记者安全以及媒体独立的措施进行评估，并形成报告。<br />
<br />
以遇难美国记者命名<br />
<br />
美国福克斯新闻网等媒体称，该法名称是为了纪念美国记者丹尼尔・珀尔。<br />
<br />
2002年初，《华尔街日报》驻南亚首席记者丹尼尔・珀尔在巴基斯坦的卡拉奇遭恐怖分子绑架，2002年2月1日被斩首，逝年38岁。<br />
<br />
5月17日上午奥巴马签署该法案时，在场的有珀尔的6名亲属。奥巴马说，他尤其想谢谢珀尔一家，因为他们的亲人尽管丧生了，但他们还是非常坦率地、勇敢无畏地让联邦政府以珀尔的名字来命名该法。<br />
<br />
在15分钟的仪式上，珀尔的遗腹子亚当收到了奥巴马赠送的溜溜球。亚当在父亲遇害3个月后出生。今年5月28日，是亚当的8岁生日。珀尔的遗孀玛丽安和亚当都住在巴黎。珀尔的父亲朱迪是一名犹太人，他于20世纪60年代移居美国。他和老伴鲁斯住在美国加利福尼亚州的恩西诺。珀尔还有两个姐妹。<br />
<br />
当天的仪式还出现了颇有意思的一幕，美国哥伦比亚广播公司记者要求向奥巴马发问，让他谈谈有关新闻自由的问题。奥巴马却显然意不在此。他回答说，“你有提问的自由”，但话锋一转，“今天这里不举行记者招待会。”<br />
<br />
美参议院全票通过<br />
<br />
其实，该法案早在14个月前就开始运作了。当时，美国联邦众议员亚当・希夫打电话给珀尔的父母，问是否可以用他们儿子的名字来纪念新闻自由法案。该法案去年10月在众议院获得通过，并在上个月在参议院获得全票通过。在仪式举行的前4天，珀尔的母亲鲁斯获得通知，他们一家被邀请参加白宫的仪式。她很快召集其他家庭成员一同前往。这是珀尔的家属第三次应邀访问白宫。<br />
<br />
2007年12月，当时的美国总统布什邀请珀尔的父母参加光明节(“光明节”是犹太人最重要的节日之一)纪念活动。他们带上了一个烛台，这是珀尔的爷爷1924年从巴勒斯坦移民到美国时所带的东西。几天后，鲁斯・珀尔再次到白宫，这次是参加有关人权会议，布什也参加了该会议。<br />
<br />
他们的儿子遇难后，朱迪和鲁斯建立了“丹尼尔・珀尔基金会”，以期进一步通过全球新闻业、音乐等项目，促进跨文化的理解和交流。<br />
<br />
美国福克斯新闻网17日报道说，参议员克里斯・多德在一项书面声明中称，该法案在参议院获得全票通过。他希望丹尼尔・珀尔的悲剧不再重演。有支持者认为，该法在曝光针对记者的恶行方面前进了一大步。<br />
<br />
美国联邦众议员希夫说，他希望该法能帮美国和其他国家更好地保护那些奋斗在第一线的记者们。<br />
<br />
美国抢占道德制高点<br />
<br />
美国有线电视新闻网17日报道说，奥巴马签署该法案，扩大了联邦政府监控全球新闻自由的角色。该法案实际上是通过国务院年度人权报告，加强了审视不同国家新闻自由程度的力度。<br />
<br />
在接受《青年参考》报采访时，复旦大学美国研究中心的潘锐教授表示，奥巴马作为民主党人，他签署这一法案并不出人意料，因为民主党善于在言论自由和人权方面造声势。美国是超级大国，其一举一动牵动全球神经。美国自立国以来，就有一定的道德优越感，至少它自己这么认为。现在，美国要将这部国内法作为法律保障，在全球推广美国在该问题上的立场，这很容易对他国构成干涉内政的伤害。　　<br />
<br />
另据美国有线电视新闻网17日报道，自1992年以来，全世界已有811名记者被杀害。其中，死在伊拉克的记者最多，有142名。（青年参考）</div>

							
							
							
							
