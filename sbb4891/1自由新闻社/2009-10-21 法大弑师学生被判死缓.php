　　这件凶杀案发生在法学高等学府与课堂之内外，杀人者是法学院校的学生，被杀者是教授西方法律思想史、法理学、比较法的副教授。将所有的这些信息，放入凶杀案与废除死刑的大背景下，另一种令人窒息的空气出现了。一面是凶残的空气、同态复仇的快乐与正义的大旗，一面是翩翩起舞的宽容、理性与人性；一面是自己被剥夺生命的同僚和朋友，一面是芸芸众生；一面浮现在每个人的脸上与心中，而另一面则游荡在形而上的庙堂与不食人间烟火的象牙塔中。&nbsp;&nbsp;<br />
<br />
　　这个悖论，就像苏格拉底是否应当坦然接受不公正的死刑那样让人矛盾。而早在付成励案开庭审理的一个多月前，西南政法大学的高一飞教授就专门著《社会正义不容淡视 期待法院理性裁决课堂弑师案》一文，呼吁法院判处付成励死刑。其中的精彩言论有：&nbsp;&nbsp;<br />
<br />
　　付成励的室友“希望他能活下来。”这是基于特殊的室友情谊，不太理性。 <br />
<br />
　　希望不处付成励死刑者，原因是为了“保留那个鲜活的生命”，其实，这个理由是一个国家废除死刑的理由，所有被判处死刑者哪个都是“鲜活的生命 ”，甚至于还可能是看起来温柔妩媚的“花季少女”。我个人支持废除死刑，但只要死刑还没有在这个国家废除，就当做到罪刑相当、适用平等，维护法律尊严。&nbsp;&nbsp;<br />
<br />
　　付成励没有半点可以宽恕之处，不属于“不是必须立即执行”死刑的情况，理当判处死刑，立即执行。 <br />
<br />
　　尤其是高一飞教授提到“我个人支持废除死刑”，但是法院“理当判处（付成励）死刑，立即执行”。原来，法学教授只在课堂上和反对死刑，所有理性的、温情的反对死刑的说辞，一走入生活，就变成了死刑的坚定支持者，不仅如此，还要呼吁法院积极的判处死刑。&nbsp;&nbsp;<br />
<br />
　　这种魔鬼逻辑，在生活中是无处不在的盛行。“所有动物一律平等，但有些动物比其他动物更平等。”“取消户籍制是好的，但是现阶段不能一步到位。” <br />
<br />
　　所有美好的事务，都变成虚无缥缈的空中楼阁，可以观看，可以把玩，可以想象，可以号召其他人追求，但是就是不许触摸。法学教授，作为一个社会中的知识精英和理性的捍卫者，也很不幸的参与到了这种魔鬼逻辑的游戏中。&nbsp;&nbsp;<br />
<br />
　　是否废除死刑，对于公众而言，自然是无定论的，不同人群持有各种观点也无妨碍。付成励一案，亦可以作为一个特殊的角度引发对死刑的探讨。然而作为死刑反对者主要人群的法学家们，至今仍然保持着不恰当的沉默，甚至抛出支持死刑的言论。&nbsp;&nbsp;<br />
<br />
　　在此，不得不感慨2003年沈阳刘涌涉嫌组织黑社会性质组织一案中，刑辨律师和法学家们顶住了公众舆论的巨大压力，抛出刘涌涉嫌被刑讯逼供而需要重新考虑证据有效性的问题。反对刑讯逼供的律师和法学家，并不因为被审判者涉嫌组织黑社会而支持刑讯逼供。律师和法学家们的行为虽然被公众舆论广泛的批驳，然而从维护法律的理念与法律的尊严而言，这个行为和美国辛普森杀妻案中，嫌疑人辛普森因警方调查案件和搜集证据存在瑕疵而免于刑事处罚一样伟大。&nbsp;&nbsp;<br />
<br />
　　走出了课本的刑事案件和死刑，考量着每个法学家和法律人。甚至包括已故的程春明教授，他曾经在自由浪漫的法国和于1981年废除了死刑的法国生活过12年，他将如何在天国看待剥夺了自己生命的付成励；还有他主攻刑事诉讼法学的妻子，作为写作过《死刑的正当程序与死刑的控制》一文的法律人，她又将如何看待剥夺了自己丈夫生命的付成励。&nbsp;&nbsp;<br />
<br />
　　我期待呼吁废除死刑的法学家们，能有苏格拉底面临自己被非法判决而处以死刑的淡定，而不是将废除死刑作为一句时髦的大词，用之即来，挥之即去。 <br />
<br />
　　不反对付成励死刑的法学家，还是法学家吗？</div>

							
							
							
							
