一个水果商贩的意外死亡<br />
<br />
&nbsp; &nbsp; 2009年7月5日晚10时许，26岁的水果商贩鹿华坤死在乌鲁木齐市大湾北路东侧的路边。一群暴徒先砍掉鹿华坤的左前臂，接着向他的头部砍了数刀，鹿随即倒毙，暴徒用石块儿连砸鹿的头部，直至脑浆流出。<br />
<br />
&nbsp; &nbsp; 鹿华坤倒毙的地方，距离他的住所500米远。他只是为了回到距住所两公里远的集贸市场，取回自己摊位上尚未出售完的水果。<br />
<br />
&nbsp; &nbsp; 7月8日13时，大湾北路135号，乌鲁木齐市区东南部城乡结合部，一间简陋的民房里，记者见到了鹿华坤的父母。<br />
<br />
&nbsp; &nbsp; 鹿华坤的父亲鹿思峰面色严峻，不停地抽烟，鹿华坤的母亲躺在床上抽泣着。他们刚刚从乌鲁木齐公安局刑事侦查支队认尸回来，鹿华坤的父母及数位邻居的叙述了鹿华坤遇害的经过。<br />
<br />
&nbsp; &nbsp; 7月5日白天，乌鲁木齐大湾北路的集贸市场如往日一般平静，晚9时许，集贸市场管理处的人突然通知商户，称市内发生骚乱，大家紧急撤离。商户们懵了，但还是听从了劝告，大家收拾物品离开市场。<br />
<br />
&nbsp; &nbsp; 鹿华坤和母亲共有四个水果摊位，他们把一大堆的水果搬上两部三轮车上，鹿和母亲先将一车的水果运回了家。<br />
<br />
&nbsp; &nbsp; 此刻，9时半许，大巴扎和跃进街的骚乱向东已经扩散到大湾北路，暴乱分子出现在大湾北路上，已经砍杀了数人。鹿华坤告诉母亲，要返回市场将剩下的一车水果送回家。<br />
<br />
&nbsp; &nbsp;“我们心里想着只要不得罪人，怎么会有麻烦呢？”鹿的母亲告诉记者。她送儿子出门时，看到大街上许多过路的维族人，这些人并没有受到攻击。<br />
<br />
&nbsp; &nbsp;“谁知道他们见汉人就砍！见汉人就杀！”鹿华坤的母亲向记者哭诉着。<br />
<br />
&nbsp; &nbsp; 等了很久，儿子没有回家，鹿华坤的母亲心慌了。但是，远远望去，大街上暴徒还没有散去，她又不敢出门去寻找。一直捱到12时，暴徒渐渐散去，成队的出现在大街上，鹿华坤的母亲才敢出门，不久，她发现了倒毙在路边的儿子。<br />
<br />
&nbsp; &nbsp; 对于这位母亲来说，剩下的事情，只有抚尸痛哭。7月6日凌晨3时，在警察的帮助下，鹿华坤的遗体被抬走，与大湾北路该地段上的其他10多具遗体堆在一起，凌晨5时，鹿华坤的遗体被运走。<br />
<br />
&nbsp; &nbsp; 鹿思峰6号从西安返回乌鲁木齐，7号，整整一天，他竟没有从公安局的录像里认出儿子的遗体，因为多数遇害的遗体血肉模糊，只有凭借身上的衣物辨认，鹿思峰有较长一段时间不在家，不知道儿子当天穿的衣服。8号上午，鹿思峰夫妇俩赶到公安局，终于完成了认尸。<br />
<br />
&nbsp; &nbsp; 当着记者的面，鹿思峰极力住按耐内心的悲愤。但是，当目光一触到手中鹿华坤的相片，鹿思峰的眼泪便流下来。流泪的时候，鹿思峰仰着头，鼻孔翕张，发出“呜呜”的声响。<br />
<br />
&nbsp; &nbsp;“我们犯了什么错？没有我们，新疆会有今天吗？没有我们，维族人还烧着牛粪呢！”鹿思峰低吼的声音里，充满了愤怒。<br />
<br />
&nbsp; &nbsp; 鹿思峰一家是河南省周口市沈丘县白集乡的农民，15年前，鹿思峰夫妇到乌鲁木齐做水果买卖，打拼生活。10年前，鹿华坤初中毕业后来到乌鲁木齐。<br />
<br />
&nbsp; &nbsp; “华坤真的是一个好孩子，非常热情善良，走在路上，看到谁的车子重，就是上去推一把，出出力。”鹿家的一位邻居告诉记者。鹿华坤是鹿家的大儿子，鹿思峰的次子还在河南老家读书，三子在新疆境内的解放军服役。“他应该还还不知道华坤的消息。”鹿思峰告诉记者。<br />
<br />
&nbsp; &nbsp; 目前，鹿思峰一家租住在两间10多平米的民宅里，一间鹿思峰夫妇俩的卧室兼水果仓库，另一间是厨房兼鹿华坤的卧室。鹿华坤房间的大床上还铺着大红色崭新的床单，这是为了迎接鹿华坤即将到来的新娘，鹿华坤已在河南老家定亲，预备在年底腊月28完婚。<br />
&nbsp;&nbsp;<br />
--------------------------------------------------------------------------------<br />
<br />
深入维族家庭<br />
<br />
&nbsp; &nbsp;红裙小姑娘的维族舞<br />
<br />
&nbsp; &nbsp;7月8日，乌鲁木齐渐露生机。人民路以北的街道上行人多了，大商场依然关门，有些小店铺却恢复营业。上午10点，我和其他媒体记者结伴，6个人，一起走到解放南路的大巴扎地区，虽然大巴扎大门紧闭，武警站岗，但旁边出现了卖西瓜、油饼的维族小贩。<br />
<br />
&nbsp; &nbsp;天气非常晴朗。我想起中学时读过一个陕西作家（名字我忘记了，在新疆当过骑兵，写过《最后一个匈奴》这本书）用过的一个词：“中亚细亚的热烈的阳光”。<br />
<br />
&nbsp; &nbsp;我们闲逛到外环路东华大市场对面时，一位头戴维族帽的老大爷，大概60多岁，手里牵着一个仅有他胯部高的小姑娘，走在我们的前面。<br />
<br />
&nbsp; &nbsp;那小姑娘穿一身纯红色的连衣裙，颠颠儿的跑着，嘴里哼着歌，双臂举起，再将两双小手放在胸前，头部随之有韵律的摆动。这可能是她刚刚学会的维族舞。<br />
<br />
&nbsp; &nbsp;三个记者同时举起了相机。那老大爷回头朝我们笑，还让小女孩儿转身摆姿势。我的相机没电了，我对同行的两位记者说，你们回头一定要把这照片给我。<br />
<br />
&nbsp; &nbsp;12点，快走到人民路和外环交叉口时，一个汉族老大爷跟我们闲聊。“政府应该主动救济贫困的维族人”，这是他的政治主张。<br />
<br />
&nbsp; &nbsp;老刘名叫刘道清，70岁，身份证上的住址是新疆新源县良种连，位于伊犁的兵团某部。老刘是四川绵延射洪人，1962年参军进疆，退休后住在乌鲁木齐市的大儿子家里。<br />
<br />
&nbsp; &nbsp;深入维族家庭<br />
<br />
&nbsp; &nbsp;刘道清一看就是个正直、热情的人，我请他带我们到维族朋友家里看看，老刘欣然允诺。全是维族人的跃进街，他有一个朋友叫阿吉马木提，54岁，乌鲁木齐阜康县人，1984年来乌鲁木齐市，在跃进街开了个服装店。老刘8年前在早市上和阿吉认识，阿吉家在阜康老家还有9亩地，种玉米，老刘正好在兵团学过农业技术，帮过阿吉挑选种子。<br />
<br />
&nbsp; &nbsp;我们走进跃进街都心惊肉跳的，如果没有刘道清，还哪敢跟这里的人说话？阿吉的服装店仅有十几个平方，房屋装饰和陈列的衣服还比不上河南乡村的店铺。阿吉的老婆和两个女人在店里。见老刘带着客人来了，阿吉的老婆急忙叫一个女儿去买西瓜，又叫另一个女儿去找在附近的阿吉。<br />
<br />
&nbsp; &nbsp;阿吉很清瘦，上唇有浓密的胡子，是个勤劳谨慎的人。他把我们引到后屋，后屋和临街的服装店相连，面积相当，阿吉家四口人住在这间狭小的房间里。<br />
<br />
&nbsp; &nbsp;“7月5日下午，太还阳很高，听到大巴扎那边爆炸的声音，我们怕极了，就把店铺关了。”阿吉说。<br />
<br />
&nbsp; &nbsp;阿吉还有三个儿子，大儿子、二儿子都在阜康老家务农，都已结婚，但没有盖自己的房子，都住在阿吉的房子里。三儿子在南京给维族老板打工，卖烤肉。<br />
<br />
&nbsp; &nbsp;“9亩玉米一年最多卖1万块钱，服装店一年收入8千到1万块钱，一家10口人（加上阿吉的两个儿媳妇和一个6月11日出生的孙子），生活困难。”阿吉说。<br />
<br />
&nbsp; &nbsp;阿吉家算是有出息的。阿吉描述村里的情景：一对年轻的夫妇，平均三亩土地，每年2000块钱的收入，还要养活几个孩子。听起来颇似河南农村的情景，只有离开家乡，才能生活得好一些，怪不得韶关的工厂里招募了800多个维族务工青年。<br />
<br />
&nbsp; &nbsp;阿吉的两个儿子很难在城里找到工作，因为他们不会写汉字。服装店里的两个女儿，大一点的17岁，没有工作。小女儿13岁，在乌鲁木齐38中读初二，她对一个记者的摄像机很感兴趣，一直盯着看。两个女儿身材苗条，黑亮的大眼睛让人难忘。<br />
<br />
&nbsp; &nbsp;我们在临街的维族人家里做客，引发了街区的注意。我们坐下不到15分钟，走进来一位健壮的维族人，名叫艾克木，70岁，可是看上去年轻多了，感觉艾克木在本地颇有人望。<br />
<br />
&nbsp; &nbsp;艾克木是南疆库尔勒市和静县人，中学毕业后，1957年开始在乌鲁木齐当5年兵。21年前，艾克木重返乌鲁木齐，在大巴扎做和田玉生意，已经搞了一间很大的铺子。<br />
<br />
&nbsp; &nbsp;艾克木有两个儿子一个女儿，其中一个儿子在上海卖和田玉。“我不缺钱，经济上没困难。”艾克木说，店铺开张，碰上好卖家，会有几十万的收入。<br />
<br />
&nbsp; &nbsp;“韶关事件后没有人做群众工作”<br />
<br />
&nbsp; &nbsp; 艾克木说出了一个被忽视却非常重要的事实。<br />
<br />
&nbsp; &nbsp;“26号在韶关发生的事情，没有人解释，没有人做群众的工作，电视上不宣传、报纸上不说这个事情，政府没做这个工作。”艾克木说，“这些打工的维族人很多都是南疆人，发生械斗后，你说是死了2个，我说死了20个，结果越传越多，造成了影响太坏了。”<br />
<br />
&nbsp; &nbsp;艾克木表情和手势非常丰富，“政府要是出来说就没事了。”说完这话，他把双手摊开，无奈的笑了笑。<br />
<br />
&nbsp; &nbsp;可以想见，6月26日到7月5日，10天里，韶关的消息是如何在新疆不可思议的传播的。就在7月8日早上，我们几个记者坐在海德酒店大堂的沙发上闲聊时，坐在旁边的几个本地人议论韶关事件，称消息传到新疆时，变成了“300多维族人被杀”。<br />
<br />
&nbsp; &nbsp;在互联网时代，封杀消息是不可能的。但这只是个浅层次的原因，深层次的原因，我认为是当地政府对基层影响力弱化。<br />
<br />
&nbsp; &nbsp;就算是覆盖面极大的体制内媒体，也没有及时报道韶关事件。<br />
<br />
&nbsp; &nbsp;艾克木向我们掏出一份形成于2005年的上访材料，大致内容是：和静县原一建公司的40多名职工，社会保险、退休手续都没有解决，生活无着。经新疆华龙会计师事务所审计，查出该公司有一些外欠的款项，但没有任何单位和人插手回收这些外欠款。<br />
<br />
&nbsp; &nbsp;材料要求政府恢复职工身份，依法补交社会保险金，办理退休手续，彻底清算公司资产，拿出一个有效可行的方案解决职工的生活就业问题。<br />
<br />
&nbsp; &nbsp;“这件事跟我没关系，我就是帮他们的忙。现在职工一上访，就被抓起来。和静县有10多个事情都解决不了（意思是跟此事件类似的事情）。”艾克木说。<br />
<br />
&nbsp; &nbsp; 南疆的小县城，也有国企改制的遗留问题。<br />
<br />
&nbsp; &nbsp; 王力雄在分析西藏问题的著作里，提到毛泽东时代大批青年干部进藏，深入农村，深入群众，这样的场景，现在很难看到了。我没去过西藏，也没到过新疆的农村，但我深深认同王力雄的观点，基层没有人，没有被群众充分信任的人，与政府高度一致的到维族群众中去解释韶关事件，并取得后者的理解。虽然这只是个猜测。<br />
<br />
&nbsp; &nbsp; 不能种下仇恨的种子<br />
<br />
&nbsp; &nbsp; 7月5日惨绝人寰的暴行，在汉族人内心的形成了巨大的影响。乌鲁木齐汉族集中的街区里，广播车里不停地播放王乐泉7月7日的讲话，《王书记要求大家回家去 回单位去 回社区去》。<br />
<br />
&nbsp;&nbsp;“有一些单位的汉族职工、群众又组织起来，有的甚至走上街头，把本来已经基本正常的社会秩序搞得乱哄哄，有的甚至凭感情用事，同维族群众对立起来。”<br />
<br />
&nbsp; &nbsp;我前文中提到的那个几被灭门的粮油店，仅剩一个10岁的女孩儿，这个女孩儿当时很幸运，不在店里面。<br />
<br />
&nbsp; &nbsp; 张明营的头被砍掉，腿被卸下来，暴徒砍杀其他人后，焚烧了店铺，张明营的躯体被烧得仅剩一个头骨，而张明营妻子的遗体一直没有找到，应当是被烧成了灰烬。10岁的女孩仅能采血做DNA检测，方能辨识这些父亲的遗骨。这个小女孩儿还不知道全家人都走完了，她的亲戚一直瞒着她。<br />
<br />
&nbsp; &nbsp; 走上街头的汉族人，都发出这样一个疑问：“当时为什么没有采取有效措施及时的制止暴徒？”<br />
<br />
&nbsp; &nbsp; 惨死在大湾北路的鹿华坤，他的家和新疆维吾尔自治区公安边防总队一路之隔。在乌鲁木齐幼师所在的街道上，一家被洗劫一空烧得熏黑的超市，仅和乌鲁木齐公安局一交警大队仅隔一个路口。团结路上，有一个几层楼的派出所，这条路仅有区区数公里，为什么暴徒还能在此肆虐？<br />
<br />
&nbsp; &nbsp; 多方汇集的消息显示，暴乱发生时，手持盾牌和警棍的武警20分钟内就出现在了二道桥，当时武警一直采取守势，并没有上前驱散暴徒。暴徒并没有拿枪，他们只有少量的砍刀和砖头、石块儿。<br />
<br />
&nbsp; &nbsp; 还有，7月4日互联网、手机短信就已经出现了暴徒打砸抢杀的预谋。7月8日，我们在二道桥碰到两个汉族人，其中一个称他的朋友，娶了维族妻子，他妻子的手机在7月4日就收到了煽动短信。<br />
<br />
&nbsp; &nbsp; 7月8日下午6点15分，周永康来到人民广场检阅武警部队。<br />
<br />
&nbsp; &nbsp; ……<br />
<br />
&nbsp; &nbsp; 至少，应该给无辜死亡的百姓一个公正的合理的交代。否则，仇恨的种子俨然种下，汉族人也会拿起砍刀。种子俨然种下，汉族人也会拿起砍刀。<br />
