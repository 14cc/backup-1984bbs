&nbsp; &nbsp;“很好，很好。”莫合烟摊上的一位年老的维族人回答道。老人用讥讽的眼光注视着我们。<br />
<br />
&nbsp; &nbsp;下午5点半，环球酒店13楼，善后工作在这里进行。贴着“医疗组”标示的一个房间最为忙碌。乌鲁木齐卫生局组织各个医院，将正在救治的昏迷不醒的人拍照，汇集在医疗组的电脑里，请家属辨认。<br />
<br />
&nbsp; &nbsp;“这些昏迷的人一般都是颅脑被损伤，脸部通常会发生变形，所以辨认有些困难。”一位工作人员介绍，“9号认出了223人，今天截止到现在（6点）认出130人。”<br />
<br />
&nbsp; &nbsp;“我儿子没有胡子，这张不是他！”一位甘肃天水的老大爷喊道。老大爷叫马万成，66岁。<br />
<br />
&nbsp; &nbsp;马万成的儿子马云明，24岁，新疆石油学院勘探开发系09年的毕业生。<br />
<br />
&nbsp; &nbsp;7月5日下午，马云明去科信学院拜访朋友，晚9点20分，马万成接到儿子的电话，马云明在电话中急促的说：“乌鲁木齐反了”，过了20多分钟，9点41分，马万成又接到儿子的电话，马云明称自己躲进了附近的一个小区，暂时较安全。可是，从此之后，马云明的手机处于关机状态，再也没有消息。马万成遂于6号从甘肃赶来寻找儿子。<br />
<br />
马万成的儿子是个好学生<br />
<br />
&nbsp; &nbsp; 马云明已经办完离校手续，他被中石油下属位于克拉玛依的西部钻探公司录用，并订好了6号上午11点去克拉玛依的车票。<br />
<br />
&nbsp; &nbsp;“马云明是个好学生，同系的80多个毕业生，西部钻探公司要了20多个，他是其中之一。”陪同马万成的新疆石油学院勘探开发系党委书记告诉我。他介绍，该校的在校生里仅有几个学生受了轻伤。<br />
<br />
&nbsp; &nbsp;马万成和马云明的两个姐姐将所有的照片看完，都没有认出马云明。现在，剩下两种可能：尸体已被烧焦无法辨认；或者失踪了。<br />
<br />
&nbsp; &nbsp;“只有等DNA检测结果了。”该系党委书记说。马万成大哭了起来，马云明的两个姐姐站在一边哽咽着。<br />
<br />
&nbsp; &nbsp;“我儿子长得很漂亮，高高大大的，他是我们家里的一座山！现在山倒了！”马万成喊道。<br />
<br />
&nbsp; &nbsp;我的眼里已有泪水。我无法再问下去，这样太不道德。马万成上身穿着一件白背心，倚在走廊的墙上。我距离老人最近，我的右手用力扶着他的左臂，想努力架住他，但是那臂膀软绵绵的，老人已经失去依靠。<br />
<br />
&nbsp; &nbsp;大家都沉默下来，老人被党委书记扶着下楼休息。过了一会儿，马云明的姐姐请求新疆电视台的记者不要播放父亲的镜头，因为马云明的母亲还不知道这里的情况。<br />
<br />
&nbsp; &nbsp;6点39分，医疗组的人告诉马云明的姐姐，最后一批汇集的照片到了，请她们过来辨认，希望能找到。<br />
<br />
&nbsp; &nbsp;“昨天病危6个，病重4个，今天病危还是6个，病重5个。”医疗组的人员在电话中记录情况。<br />
<br />
&nbsp; &nbsp;“马万成这样找不到人亲人的，大概有30多人。”一位工作人员说。<br />
<br />
&nbsp; &nbsp;晚上，回到宾馆，有一些在医院采访的媒体朋友介绍，有些家属在各大医院找了三四天，都没有找到亲人。<br />
<br />
&nbsp; &nbsp;11号早上，我打电话给该系的领导。<br />
<br />
&nbsp; &nbsp;“还没有找到。”他说。<br />
 <br />
&nbsp; &nbsp;做生意的维族人友好地收留了她<br />
<br />
&nbsp;&nbsp;“高层没有预判到事件的严重性，这是其一；其二是事件发生后，没有能够采取有效的措施将暴徒驱离、控制。即使不开枪，用高压水笼头、用催泪瓦斯震慑并努力驱散他们，可惜这些都没有做。”<br />
<br />
&nbsp;&nbsp;这是我10号下午拜访的一位人士所言。<br />
<br />
&nbsp;&nbsp;这位人士介绍，分裂分子的情报，当局肯定事先获悉。7月4日，自治区有关部门连续几天做新疆大学学生的工作，新疆大学的老师、干部堵在路口，不让学生上街，工作一直作到5号凌晨2点。5号下午游行的学生，很多是从学校里翻墙出来的。<br />
<br />
&nbsp; &nbsp;“就像以往的暴乱一样，先是游行，所谓和平情愿，然后突然一下子钻出暴徒，开始打砸烧杀。这次也是这样，下午两点，就是200多个学生上街游行，但是突然窜出几千人来，开始杀人，引发骚乱。”该人士称。<br />
<br />
&nbsp; &nbsp;他解释，当局也搞得措手不及，如果采取严厉措施，又害怕国际舆论。<br />
<br />
&nbsp; &nbsp;如此大规模的针对乌鲁木齐汉族平民的袭击，是第一次。“7・5”事件，是新疆的历史性事件，甚至是新疆历史的分水岭。这是该人士的感受，我也深表认同。<br />
<br />
&nbsp; &nbsp; 近代以来，新疆分裂势力发动的历次暴乱，都有针对平民的屠杀。<br />
<br />
&nbsp; &nbsp;新疆人民出版社1999年出版的《新疆反对民族分裂主义斗争史话》显示：<br />
<br />
&nbsp; &nbsp;1930年代“东突厥斯坦伊斯兰共和国”的暴乱中，“在莎车他（麻木提）又支持其心腹四团团副伯沙克尼亚孜捕杀团长沙衣木、县长阿合买提江等人，洗劫莎车县政府、税务局、银行库存的金钱财宝，以及人民群众的财物、马匹，县城汉人几乎被屠杀殆尽……”<br />
<br />
&nbsp; &nbsp;1940年代新疆三区革命期间，“在他（艾力汗 吐烈）的煽动下，一些地方发生了杀害战俘和汉族群众、污辱汉族妇女、抢劫汉族群众财物的事件。无辜汉族群众的生命、财产受到严重的伤害。”<br />
<br />
&nbsp; &nbsp;建国初至1980年代前，新疆分裂分子发动的暴乱，攻打党政机关，政府、公安局等目标，针对国家干部。《新疆反对民族分裂主义斗争史话》这本书里，没有显示大规模的平民伤亡。<br />
<br />
&nbsp; &nbsp;1981年，喀什暴乱。“那伙暴徒手持匕首、棍棒、皮鞭，呼喊着：‘打倒汉人’、‘打到黑大爷政府’、‘维吾尔斯坦共和国万岁’一类发动口号，横行于街市……在这次打砸抢事件中有六百多名无辜无辜汉族群众被打，其中两百多人致伤，2人死亡。”<br />
<br />
&nbsp; &nbsp;1990年代，分裂势力渗透到北疆。<br />
<br />
&nbsp; &nbsp;1997年，伊宁暴乱。“1995年以来，新疆跨地区的民族分裂主义‘东突厥斯坦伊斯兰真主党’头目排祖拉和一些重要骨干多次到伊犁活动，并派出二十多名南疆籍成员以讲经为名到伊犁进行所谓‘圣战’宣传，鼓吹民族分裂主义，发展成员和建立组织。……2月5日北京时间10时30分，数百名维吾尔族青年举着用阿拉伯文书写的两条横幅……非法游行，……12时许，约千余人集结在市人民医院门前……参加游行和闹事的人并不固定，此来彼去，时聚时散，其中夹杂着不少被裹胁蒙蔽的群众。为教育受蒙蔽者，在现场执勤的公安民警对非法游行队伍一再进行教育劝阻，但是队伍中的一伙暴徒非但不思悔改，反而以为我公安民警软弱可欺，……闹事的态势陡然升级，暴徒们手持棍棒、刀子、砖头、石块袭击在现场执勤的公安民警……2月6日，一小撮民族分裂分子……见汉族群众就打，遇汉族同志开的车就砸就烧，谁阻止他们的破坏活动就打谁、杀谁，杀人放火，无所顾忌。一伙暴徒在胜利路被服厂门前，用石头、棍棒把一位五十多岁的汉族人活活打死。在市人民医院十字路口，2名汉族人横遭暴徒毒打，其中一名被打得遍体鳞伤，气绝倒地，当场死亡，另一名被打成重伤，倒在血泊之中，奄奄一息……”<br />
<br />
&nbsp; &nbsp;伊宁“2・5”事件中，“198名无辜群众被打伤，其中50名重伤；7名汉族群众被残杀”。历史有着神秘的延续性。这情景和7月5日发生在乌鲁木齐街头的场面是多么相似！<br />
<br />
&nbsp; &nbsp;11号下午，我碰到一个主动来向警方提供情报的人（为了保护她，我不能透露她的名字）。7月5日7点50分，她走出二道桥地区的家乐福超市，被眼前的场景惊呆了：人群惊慌失措，到处乱窜。<br />
<br />
&nbsp; &nbsp;“我看到当时警察和暴乱分子对峙，暴乱分子已经杀了人，有些尸体倒在路边，但警察还是朝天开枪。”她说。<br />
<br />
&nbsp; &nbsp;她躲进小东梁派出所，上完厕所后，派出所的一个公安警告她，不要在这儿停，暴徒可能会攻击派出所，这里也不安全。她又从派出所出来，和另外一个女孩儿结伴，跟着人流向东跑，走到金银大道后，她发现女孩儿不见了。在东环旧家具市场附近，立交桥下，她的头部被暴徒拍了一砖。<br />
<br />
&nbsp; &nbsp;幸好没有被砸趴下，也没有暴徒上前群殴，她忍痛迅速躲进东环旧家具市场，虽然在这里做生意都是维族人，但是非常友好地收留了她，并取热水帮她擦洗伤口。<br />
<br />
&nbsp; &nbsp;她躲进东环旧家具市场的时间是晚上9点50分。“那些做生意的年轻点儿的维族人都出来看热闹，看一会儿又跑回来喝水，喝完水又跑出去看。来回很多趟。”她说。<br />
<br />
&nbsp; &nbsp;“我离开市场的时间是6号凌晨1点50分，我是被维族警察带走的。我看到警察把暴乱分子围起来，堵成一堆。”她说。<br />
<br />
&nbsp; &nbsp;她一再强调：“至少从10点到凌晨1点，金银大道上没有警察。”<br />
<br />
&nbsp; &nbsp;按照她的叙述，暴徒是从二道桥地区扩散、转移到金银大道的。这个场景和伊宁很相似，“此来彼去，时聚时散”。而且，警察都不敢开枪。<br />
<br />
&nbsp; &nbsp;但是，伊宁只是一个边疆小城。7月5日的事件发生在乌鲁木齐，自治区的首府，死亡人数还在持续上升，我的判断肯定在200人以上。<br />
<br />
&nbsp; &nbsp;有消息显示，“7・5”事件发生后，1000多个旅行团取消了新疆旅行计划，4400多名外国人取消了新疆的行程。<br />
<br />
&nbsp; &nbsp;“‘应该把事态估计的更严重一些’！”上述人士转述自治区某主要领导在近期会议上的讲话，“看来，这位领导也在反思。”<br />
<br />
&nbsp; &nbsp;每个乌鲁木齐人，每个新疆人，每个中国人，都受到了伤害。这是历史。<br />
<br />
安全感<br />
<br />
&nbsp; &nbsp; 以下是作为一个人，而非一个记者的一点感想！卸掉职业的枷锁，我感觉思绪变得顺畅！<br />
<br />
&nbsp; &nbsp; 我7月8日凌晨才降落在乌鲁木齐，错过了7月7日的场景。那一天，乌鲁木齐几乎所有的汉人都手持棍棒。一位记者拍到了这样的照片：医院的大夫、女护士拿着棒子在医院里走动；一个过马路的妇女，一手抱着婴儿，一手拿着棒子；还有一个年轻的女孩儿，一手拿着坤包，另一只手里掂着一个锈迹斑斑的铁锨头。这铁锨头或许是她随手从大街上捡来的，但她相信只有这件东西能给她带来安全。<br />
<br />
&nbsp; &nbsp;这些暴徒越过诸多政府机关的门口，越过交警大队的门口，越过派出所的门口，越过新疆维吾尔自治区边防公安大队的门口，将一家5口人砍头、肢解、焚烧。那10岁的小女孩儿不在家，幸存了。<br />
<br />
&nbsp; &nbsp;鹿华坤的父亲，一个坚强的河南汉子，他的生活依然很困顿。采访结束，临走时，我掏出二百块钱，塞到他手里。我解释说：“作为记者，我采访完了，履行完了自己的职责。这钱是我作为老乡的一点心意，一定要收下。”这个汉子死活不要，硬是把钱推到我兜里。<br />
<br />
&nbsp; &nbsp;对这样的人民，还能有什么要求？<br />
<br />
&nbsp; &nbsp;是该深刻反思的时候了！血怎么可以白流？<br />
<br />
