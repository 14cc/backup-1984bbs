亲历劫后的乌鲁木齐<br />
<br />
&nbsp; &nbsp; 7月8日上午8时，乌鲁木齐解除了交通管制。交通管制时间为7月7日晚9时至7月8日早8时，新疆维吾尔自治区和乌鲁木齐市的政府部门在此时间内加大抓捕嫌犯力度，扫清路障，抓紧处理善后事宜。<br />
<br />
&nbsp; &nbsp; 解除交通管制的乌鲁木齐市遍布荷枪实弹的军警，企事业机关也都处于放假状态，记者所到之处，街道上行人稀少，两侧的商铺悉数关门歇业。寻找了半个多小时，记者才在距自治区党委所在地不足1公里处找到一处小饭店吃早餐，而在此小饭店50米处的路口，就有两队大约20人的武警小分队列队把手，武警人员头戴钢盔，手持钢制盾牌、警棍和微型冲锋枪。<br />
<br />
&nbsp; &nbsp; 乌鲁木齐出租车多数停运，大多数汉族人司机不愿到市区维族人聚居地区。8日上午10时，记者离开乌鲁木齐市区的北部（党政机关、企事业单位集中区），步行穿过人民路，来到维族人聚居的二道桥（大巴扎）、跃进街地区，发现这里一反市区北部的萧条景象，道路熙熙攘攘，来往行人络绎不绝。跃进街两侧聚集着本地的维吾尔族人，在靠近乌鲁木齐妇幼保健院的街区，有些三两个人围在一起，穿着拖鞋，坐在路边交谈，慵懒着打量着街道上的陌生人，记者感到他们目光平静，没有敌意。<br />
<br />
&nbsp; &nbsp; 跃进街一处水果鹏的地上，堆满了大个的西瓜，人们争相购买。街边的蔬菜摊、馕饼店挤满了人，这里的生活再继续。<br />
<br />
&nbsp; &nbsp; 但这并不意味着劫波已散。下午4时许，记者经过跃进街中环路以东的街区，尚有三部被烧毁的汽车没有清理，两部轿车，一部厢式货车，黑灰色的残骸推在街道中心，摄人心魄。这条街曾是暴乱分子聚集的地方，恐怖的痕迹犹在。<br />
<br />
&nbsp; &nbsp; 一位出租司机告诉记者，暴乱发生前没有任何迹象，乌鲁木齐非常的和谐、安静。暴乱发生的非常突然，当时普通老百姓并不知道事态的严重性，没有丝毫的防备。暴乱分子看到汉族面孔的人就殴打、砍杀，打砸抢的店铺，都是汉族人的店铺，凶器有自身携带的一尺多长的砍刀，还有就地取材。<br />
<br />
&nbsp; &nbsp; 乌鲁木齐团结路上的电影发行公司大门被暴徒到会，上午11时，记者路过时，大门正在修理。记者通过乌鲁木齐电视台播放的一段视频看到了7月5日晚大门口发生的恐怖场面：十几名暴徒将一位受害者追打到门口，受害者已倒在门口，十几名暴徒遂照着这名受害者胸腹部猛踢两分钟左右，踢打后，受害者趴到地上，一动不动。暴徒将受害者身上的钱包、手机抢掠后，分散离开。<br />
<br />
&nbsp; &nbsp; 团结路是汉、维混居的街区，汉人店铺的玻璃，悉数被砸烂，而紧邻的维族人店铺却安然无恙。乌鲁木齐教育研究中心门外的人行道上，有两处半米见方的发黑的血渍，有几款地砖还留在血渍上。这地砖就是铺设人行道的砖头，暴徒从人行道内取出，直接使用。<br />
<br />
&nbsp; &nbsp; 这位司机是回族人，他妻子的姐姐也是回族人，暴乱发生的时候，因为穿着普通的衣服，不敢走路回家，只得借了一个维族妇女的黑头巾，方才脱险。<br />
<br />
&nbsp; &nbsp;7月8日4时，在乌鲁木齐幼儿师范学校门口东侧200米处，一处烧毁的民宅，全部坍塌，物品和灰烬混在一起，唯有残留的后山墙上堆积着的面袋。这是一处粮油店，店内一家5口都被烧死。<br />
<br />
&nbsp; &nbsp; 附近的住户向记者介绍，当时10多明暴徒冲进店里拖出三两个人，用棍棒对其进行殴打，后又将北大的人员拖进店中，打人的暴徒出来后，便看见店里冒起了浓烟。<br />
<br />
&nbsp; &nbsp; 店主名叫张明营，是河南省周口市沈丘县人。死亡的另四人中，有张明营84岁的母亲，13岁的儿子，以及27岁的外甥。张明营的妻子当时也在场，但一直找不到她的遗体。记者在现场尚能闻到刺鼻的臭味。<br />
<br />
&nbsp; &nbsp; 西虹东路1099号，乌鲁木齐公安局刑事侦查支队，暴乱遇害者遗体就存放在这里。该机关禁止记者进入，也不让记者在门口处驻足。下午3时许，一位浙江人，42岁，开车经过二道桥遇害，家人前来认尸。妻子已泣不成声，瘫倒不能走路，在十几人的簇拥下，被三个人驾着走出大门。该浙江人已经在乌鲁木齐做生意10多年。<br />
<br />
&nbsp; &nbsp; 7月8日下午3时至4时，记者坐在出租车里，听到广播里播送的寻人启示，5分钟里至少有15个人启示，各个年龄阶段的，都在7月5日晚上外出，从此与家人失去联系。<br />
<br />
&nbsp; &nbsp; 幸福路与跃进街东段交叉，幸福路虽然只有1公里长，但基本上都是汉人店铺，7月6日、7日连续两天，汉人店铺的老板手持铁棍和木棍，袭击从这里经过的维族人。下午4时，记者看到幸福路店铺里的墙上贴着许多红纸条：“保持冷静，克制理智”。<br />
<br />
&nbsp; &nbsp;“这样做也不对，暴徒应该都是从外地赶到乌鲁木齐来的，本地的维族人不会参加暴乱，我住的小区里的维族人就没有一个参加的。”上述司机告诉记者。<br />
<br />
&nbsp; &nbsp; 7月8日下午4时许，上千名汉族人聚在解放路和中山路交叉口，情绪激烈。“你们的家人没有被打过！你们不理解！”一位示威者这样大声告诉警察。 <br />
