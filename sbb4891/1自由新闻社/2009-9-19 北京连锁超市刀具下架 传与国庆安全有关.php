													<h2>新京报+《部分超市菜刀停售 》</h2>
						
						
																											<div id="postmessage_249736" class="t_msgfont">家乐福、沃尔玛等超市刀具已下架 <br />
田北北 <br />
 <br />
 <br />
　本报讯 （记者田北北）临近国庆，北京一些大型超市的菜刀全部下架。昨日，记者走访家乐福、沃尔玛等超市发现，超市内的菜刀已全部下架。警方表示，没有下达任何命令要求超市停售菜刀。<br />
<br />
　昨日下午，记者来到家乐福国展店，在二楼的厨具专架已找不到菜刀的身影，就连水果刀、裁纸刀等刀具也难寻踪迹。一名工作人员讲，临近国庆，他们接到上级命令，“十一”前和“十一”期间不得出售菜刀等刀具。而在家乐福望京店，菜刀也已下架，得到的答复同样是临近国庆菜刀停售。<br />
<br />
　在沃尔玛超市知春路店，菜刀也已停售。该超市一名值班经理表示，他们接到公司上级通知，国庆期间为确保治安，菜刀被临时下架；如果购买菜刀等刀具，可到小型日杂店和市场购买。<br />
<br />
　记者随后致电美廉美超市，工作人员称，未接到菜刀下架的通知，目前仍在销售。<br />
