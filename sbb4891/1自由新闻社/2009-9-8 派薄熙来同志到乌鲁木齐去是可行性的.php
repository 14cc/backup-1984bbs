<br />
　&nbsp;&nbsp;薄熙来同志最近唱红打黑出来很大的名。尽管为事实证明，红的未必真红，黑的亦非全打。但是，就社会影响力而言，薄熙来已然成为高举毛泽东思想伟大旗帜的体制内代表性人物。其红色短信，其不时冒出的主席语录（请注意，有一个半个是伟大十年期间的吗？），其与普通市民吊膀子式地谈话办事风格，是今日之僚属中不可多得的一块材料。<br />
<br />
　　全部问题在于现实力量纠结中，你有没有可能有自己的领袖自己的力量去解决问题？没有，是吧？既无武器批判的可能，有无法集中精力拿起批判的武器。于是，只能把希望寄托在别人身上，寄托在那些据说是英雄老子的好汉儿身上。<br />
<br />
　　主席有知，也许会仰天长叹。<br />
<br />
　　所以，现在只能让薄熙来去，敲锣打鼓地欢送他去。<br />
<br />
　　如前所述，薄熙来有红帽子，红帽子是民族兄弟的情结。为什么民族兄弟之绝大多数今日还在家中悬挂主席像，还把豆大的毛主席万岁标写在山坡上，还在怀念与伟大领袖的远远的一面，还在羡慕那骑着毛驴上北京的库尔班大叔？民族兄弟看不起有些人，是应该的，我们教育传媒多少年来以摸黑辱没编造践踏毛主席及其伟大时代为能事。让人看不起，活该！薄去了，至少是个中和。<br />
<br />
　　还有，薄熙来比某些大领导在领导艺术上要纯熟得多，不会当面流眼泪，背后下毒手，不会太明显地损着穷人的牙眼还给你伤处抹最好的止痛剂。不会让人恶心，不至于引起反感。这也是很重要的。<br />
<br />
　　具体来说，此次新疆群体事件，大而言之，是路线问题。小的说，就是社会治安，也就是打黑除恶不力所致。别的不说，有些南方人自己治不好厂子，管不好班子，把问题放任到西部去，这才是真正的政治伦理缺乏的表现。但是，重庆这次打黑，雷声大，呼声高，对犯罪分子尤其是没几个大爪子的贪官污吏有威慑力，对老百姓也是个很好的安抚器。<br />
<br />
　　不要老往新疆跑，有害无益。派一个得力的得人心的市委书记，就成了。<br />
<br />
　　要相信我们新疆人民群众的百分之九十五，要团结我们新疆干部的百分之九十五，这些事请，同时需要得到全国各界群众百分之九十五的支持，就目前局面言之，只能选择薄熙来。<br />
