													<h2>凤凰论坛好文一枚：回国后才知道美国究竟畏惧中国什么？</h2>
						
						
																											<div id="postmessage_102224" class="t_msgfont">生活在美国，常常可以在新闻媒介中看到有关“中国威胁”的论调，比如中国的纺织品出口，比如中国的石油进口，等等，我常常在想，到底美国政客们的内心深处，对中国的畏惧是什么？　　<br />
<br />
　　我最近回了次国，所见所闻，对美国人内心真正的畏惧，开始有了点了解。在广州，因为我需要一个小型的DVD播放机，到各大电器卖场走了走。有一种特别吸引我的注意，这种播放机还可以直接收电视、接电脑、下载MP3，甚至可以连接汽车卫星导航系统，这么多功能，才卖2000多元人民币（合300美元左右）。因为有制式和保修的问题，按照常理判断，美国应该也有同样的播放机出售，价钱应该差不多，因此我决定回到美国再买。　<br />
<br />
　　谁知道回美国，到各大电器卖场转了一圈，找遍了所有型号的机子，包括打美国牌子从中国进口的，没有一款是多功能的，我在想，如果在中国卖的播放机能进口美国，价格再降一点，还不把这里的市场冲垮吗？联想到近年来美国进口的电器中，尤其是冰箱、空调、电视机、数码相机、电脑等等，中国产的比例越来越高，质量越来越好，价钱越来越低，再联想到美国的一些政客在嚷嚷要对中国的商品进口设限，我忽然明白了一点，其实美国不是害怕中国的某种产品，而是害怕中国在这方面尤其是技术发展的凶猛势头。　<br />
<br />
　　DVD播放机这个例子，我想在美国并不是特例，以“世界工厂”出名的中国，其实在生产过程中，不但质量在突飞猛进，产品档次也在不断提高，我十多年前来美国时，中国产的餐具，最多只能当一般日用品来卖，现在许多名牌餐具已经在专卖店中出售，而价钱却降低了一半以上。　　<br />
<br />
　　我们这次到北京旅游，住在小姨子家里，因为工作忙，她基本上是不到超市买菜的，隔三岔五打个电话：“我要八个鸡蛋”，或者“我家的过滤水没了，我要一罐水”，于是就有人送上门来，不需小费，也不需额外收费。我在广州也一样，没时间出去吃饭时就打个电话叫餐，送上门的价钱和在饭店买一样。　　<br />
<br />
这种服务在中国司空见惯，在美国可是奢侈中的奢侈，送上门来，不但连送费，而且要加小费，最后加起来的价钱可能是商品价的一倍半甚至两倍，更糟糕的是，美国的许多商店和超市是不提供送货上门的，因为他们工资太高，雇不起这么多人。<br />
<br />
　　我这次回国，深感服务素质比过去大有提高，饭店里服务员的服务基本上到位，大部分商店里售货员对自己负责的商品了如指掌。在北京和广州，出门旅游可以在网上订票，可以有人送票上门，也可以在自家附近的售票点买票，方便得很，各大旅游景点前可以很方便地找到导游。要找吃的吗？随处都有饭店，坐下来和服务员聊聊，一会儿热腾腾的饭菜就上来了。<br />
　　<br />
　　想起在美国旅游，除了参加旅行团外，如果要自己玩，从订票到租车，到查旅游路线图等等，都可以自助式在网上完成，方便是方便，要享受处处“有人，有人情味，有亲切感”的服务，无疑是一大奢侈，到了景点要找个导游更是难上加难，只能看着景点的路线图自己寻找，要了解景点的历史，或者知道哪里是自己最喜欢的地方，基本上是不可能的，除非你事先查过有关资料。至于吃的，美国各大旅游点的餐厅不但贵，而且几乎都是人满为患，吃饭时间，在餐厅内能找到个位子坐下来吃饱已经是谢天谢地，想便宜点吃快餐，只能跟着长蛇阵式的队伍，耐心排吧。如果有人不信，不妨到洛杉矶迪斯尼乐园内的餐厅看看，吃饭高峰时间，半个小时能买到算你运气，有名气点的餐厅，排队一个小时吃上东西一点也不稀奇。　　<br />
<br />
　 平心而论，总体而言，美国的服务业人员，素质不低，但由于工资太高，许多大公司都不可能雇用更多的人来进行人对人式的服务，许多服务项目只能交给机器或者互联网来完成，但有时候，即使短短的一声问候，可以让人如沐春风，这是机器永远不可能做到的。　<br />
<br />
　　悲哀的是，据统计，70％的美国人出门旅游还是在自己的国家内，报纸上80％左右的是美国当地新闻，因此目前大部分美国人仍然沉醉在“我们系统不是最完善的，但是世界上最好的”这样一种感觉中。对于世界的变化，不说漠然无知，也是知之甚少的。然而，清楚了解中国近几年变化的美国人，内心会有什么样的想法？我想，至少有一部分人，是心生畏惧的。ZZ</div>

							
							
							
							
