杀了你的朋友，难道我能说“那只是鄢烈山的朋友，不是全人类的朋友”？事实上 <br />
，虐狗者之所以引起公愤，原因之一，就因为他们杀了“人的朋友”。尽管这“朋 <br />
友”只是某个人或者某些人的，并不被普遍承认，但也是“人的朋友”。不难设想 <br />
，当你的朋友被活活烧死时，你会怎么样。不理解这一心理，就看不懂这一事件。 由此我们可以得出第一个结论：狗是人的朋友，不可以随便杀害。 不过此事并没有这么简单。因为我们马上就可以问：如果不是人的朋友，是不是就 <br />
可以活活烧死？比方说，如果被烧死的是老鼠呢？是不是就无所谓了？这正是我们 <br />
要讨论的第二个问题── <br />
二 老鼠就可以活活烧死吗 可能很少有人想过这个问题，也许有人会认为烧死无妨。实际上，如果这回被烧死 <br />
的是一窝老鼠，就决不会引起如此强烈的反应。这几乎是可以肯定的。在许多人看 <br />
来，老鼠可不是“人类的朋友”，甚至还是“人类的敌人”。既然是“敌人”，那 <br />
么，格杀勿论！至于如何杀，就无所谓了。 但我以为，即便是老鼠，也不可以活活烧死的。事实上，本次事件的关键并不仅仅 <br />
在于“杀害”，更在于“虐杀”。据报道，当时目睹这一事件的小孩子被吓哭：“ <br />
他们怎么可以这样对待小动物啊！”这便正是让居民和网友怒不可遏的第二个原因 <br />
，而且是更重要的原因。 那么，“虐杀”与“非虐杀”又有什么区别？其结果，不都是剥夺了对方（老鼠、 <br />
狗或者人）的生命么？没错，就结果而言，二者并无区别。但就过程而言，却有天 <br />
壤之别。区别就在“虐杀”伴随着痛苦，“非虐杀”则不痛苦，或者将痛苦减到最 <br />
低（比如注射药物）。由于种种不得已的原因，人类目前还无法做到谁都不杀，比 <br />
如屠宰牛羊，比如保留死刑，但我们完全可以做到不“虐杀”。为什么不能“虐杀 <br />
”呢？因为“虐杀”意味着“虐待”，而“虐待”意味着“残忍”。不仅是对别人 <br />
、对象的残忍，也是对自己的残忍。一个人，如果不把虐待当回事，就有可能进而 <br />
以虐待为乐；而一个人如果竟以虐待为乐，那他就丧失了人性。所以，我们必须反 <br />
对虐待，反对虐杀。比方说，在不得不保留死刑时，坚决废止凌迟、腰斩、砍头等 <br />
虐杀方式；在无法避免战争和执刑时，坚决反对虐待俘虏和犯罪嫌疑人。 由此我们可以得出第二个结论：即便是“敌人”，也是不可以“虐杀”的。 不过，我们的话还没有说完。因为还会有人问：不能虐待人，这没有问题。但如果 <br />
是老鼠，或者是危害了人的生命安全，侵犯了人权的流浪狗，又有什么不能虐待的 <br />
呢？这就牵涉到我们要讨论的第三个问题── 三 我们为什么不能虐待动物 要说清楚这个问题，必须先说清楚我们为什么不能接受虐待。为什么呢？就因为人 <br />
性之中有一条善的底线，这就是“恻隐之心”。所谓“恻隐之心”，也就是“不忍 <br />
之心”；所谓“不忍之心”，也就是不忍心看着别人受苦受难受折磨的善心。这是 <br />
道德的底线，也是道德的起点。一个人，只要有了这样一份善心，他就有可能成为 <br />
一个好人。 但这和不虐待动物有什么关系呢？关系就在于这种“不忍之心”有一个心理依据， <br />
即由此及彼、推己及人。一个人，为什么不忍心看着别人受苦受难受折磨？说到底 <br />
，就因为自己不愿意受苦受难受折磨。这就叫“己所不欲，勿施于人”。比方说， <br />
你不愿意被烧死，你就不要烧死别人；你不愿意自己的孩子当着你的面被烧死，你 <br />
就不要当着别人的面烧死别人的孩子，哪怕这个“别人”只是一条母狗，甚至只是 <br />
一只老鼠！ 为什么我们要把这份善心扩大到动物？因为“己所不欲”容易，“勿施于人”难。 <br />
这就需要培养。而且，为了保证这种培养是成功的，我们不但必须提倡“己所不欲 <br />
，勿施于人”，还必须提倡“己所不欲，勿施于物”，即不但不虐待人，就连动物 <br />
也不虐待，哪怕这动物“丑恶”如老鼠。不难想象，一个人，如果连老鼠都不忍虐 <br />
待，他还会虐待人吗？相反，一个人，今天能够虐待老鼠，明天就能虐待小狗，后 <br />
天就可能虐待人。为了保证人的不受虐待，我们必须反对虐待动物。这不是什么 “ <br />
动物福利”、“人狗关系”或者“狗权”问题，而恰恰是“人权”问题，是人与人 <br />
的关系问题，是社会问题。 由此我们可以得出第三个结论：恻隐之心，要从善待动物做起。而且，只有当所有 <br />
人都不再忍心虐待动物时，人与人之间的和谐关系才能够真正建立起来。 或许有人会问：如果动物侵犯了人权，威胁到人的生存呢？也要善待吗？也要。即 <br />
便万不得已（比如出现鼠灾），也不可虐杀。即便不得不采取非常手段，也必须将 <br />
影响减到最小。这或许又是让人想不通的问题。看来我们还必须把话说透，这就是 <br />
── <br />
四 反对虐待动物究竟为了提倡什么 我们为什么主张善待动物？我们为什么反对虐杀动物？我认为，归根结底，就是要 <br />
提倡和培养“恻隐之心”即“不忍之心”。在这里，重要的是“不忍”二字。事实 <br />
上，在南京虐狗事件中，最让人无法忍受的是这样一幕：“一个火团在洞里滚来滚 <br />
去，母狗眼睁睁看着自己的孩子被烧死。”这才引起了人民群众极大的愤慨。孟子 <br />
有云：“老吾老以及人之老，幼吾幼以及人之幼。”谁能忍心让一个母亲“眼睁睁 <br />
看着自己的孩子被烧死”？ 然而很多人都没想到，那些虐狗者“居然下得了手”，而且还不让别人救援！这可 <br />
真是“是可忍孰不可忍”！孔子这话原本有两种解释。一种是：如果连这样的事都 <br />
能狠得下心来，还有什么事他做不出！另一种解释是：如果连这样的事都能容忍， <br />
还有什么事情不能容忍！在南京虐狗事件中，这两种解释都适用。这是让居民和网 <br />
友怒不可遏的第三个原因。 但我们决不能因此而“以暴易暴”、“以虐抗虐”，比方说“也当面烧死她的孩子 <br />
”。这是决不可以的，是必须坚决反对的。我愿以最大的善意猜测，网友说这话， <br />
其实不过是想请虐狗人设身处地地想一想，将心比心地想一想。其实不想也知道， <br />
如果自己的孩子被当面烧死会怎么样？肯定是怒不可遏。如果眼睁睁地看着孩子被 <br />
别人烧死，而自己却无能为力，又会怎么样？肯定是悲痛欲绝。既然如此，那你们 <br />
为什么要对另一位母亲和她的孩子下手？难道仅仅因为那母亲和孩子是狗？难道因 <br />
为它们是狗，就没有母爱和亲情，就没有恐惧和痛苦，就没有免遭虐待的权利，就 <br />
可以任人宰割么？ 这些问题，虐狗的那四位公民和同胞，你们想过吗？振振有词地为他们辩护的鄢烈 <br />
山先生，您想过吗？还有那些仅仅把问题归结为“无法可依”的评论家们，大家都 <br />
想过吗？ 显然，虐狗事件在拷问着我们。拷问着我们的人性，也拷问着我们的国民性！</div>

							
							
							
							
