																											<div id="postmessage_426270" class="t_msgfont">一线城市二手房成交量全线崩溃 市场陷入僵局 <br />
 来源：大洋网-广州日报&nbsp;&nbsp;作者：赖伟行2010年01月12日<br />
[提要] 2009年12月，一线城市房价继续上扬，但成交量却全线下跌。业内人士认为，目前市场观望情绪正在蔓延，二手房住宅市场陷入僵局。预计一月至二月下旬的春节前后，国内二手房市场成交量仍将继续走低，甚至可能大幅下滑。到时，国内越来越多城市的房价将有可能下探。&nbsp;&nbsp;<br />
<br />
<br />
　　昨日，中原地产发布上月二手住宅指数月报。数据显示，上月，国内二手住宅市场出现微妙变化：一线城市房价继续上扬，但成交量却全线下跌。中原监测数据显示，12月份各地新增购房需求大幅回落，市场观望情绪浓厚。 <br />
<br />
　　中原地产研究中心研究经理瞿安新认为，目前市场观望情绪正在蔓延，二手房住宅市场陷入僵局。<br />
预计一月至二月下旬的春节前后，国内二手房市场成交量仍将继续走低，甚至可能大幅下滑。到时，国内越来越多城市的房价将有可能下探。<br />
　　中原的调查数据显示：去年12月京、沪、穗、深、津五大城市二手住宅价格延续全年楼市的惯性继续上扬，环比涨幅均超过2%。上海涨幅最高，达3.88%；广州位居第二，为3.23%。因此，近期二手住宅市场卖方心态仍然较为坚挺。不过，“营业税征收年限2改5”对二手住宅市场影响仍较为显著。<br />
<br />
　　二手房成交量全线崩溃<br />
<br />
　　五大城市二手房住宅市场成交量正在全线溃退，深圳下跌幅度最大，达到21.55%，北京次之，为21.38%。北京、深圳和天津三城市的成交量下滑幅度均超过20%，上海和广州下滑幅度相对较小。<br />
<br />
　　租金回报率再创新低<br />
<br />
　　相比较，租金回报率再创新低。五大城市中仅有广州租金回报率超过3%，其他城市均不足3%。天津租金回报率首次跌破3%至2.99%，在五大城市中成为第四个租金回报率不足3%的城市。</div>

							
							
							
							
