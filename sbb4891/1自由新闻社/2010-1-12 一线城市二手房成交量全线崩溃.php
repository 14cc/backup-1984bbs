去年《福布斯》判断的依据是中国政府的债务。《福布斯》将央行和地方政府、各类政策银行的有关债务加总，得出结论是我国政府负担的债务为GDP的70%，大大超过美国50%的水平。<br />
<br />
中国经济在金融危机来临时，使用了过多的激素，如今聚集在体内成为毒素，中国经济亟须排毒。<br />
<br />
查诺斯的看法与美国主流经济学家不符，后者正在谨慎地欢呼中国经济引领全球经济走出衰退。但作为久经沙场的市场人士，查诺斯的直觉是敏锐的，他闻到了中国信用过度膨胀与债务缠身的不良气息。如果人民币是国际货币、境外资金可以直接做空中国市场的话，查维斯会毫不犹豫地做空中国的银行股、地产股、建筑类、有色金属股，因为这些行业只要泡沫缩小就会遭到沉重的打击。2009年价格的上升只是信用过剩与囤积的产物，而不是实体经济基本面复苏的标志，实体经济总有一天会露出本来面目，此时就是查诺斯再次收获的大好时机。就像他做空安然是因为安然怪异的财务报表一样，他看空中国经济就是因为过剩的产能与过多的信用之间存在差距，必然导致恶性泡沫的爆发。<br />
<br />
查诺斯与中国政府的对抗未必能赢，就像索罗斯在东亚金融危机期间对香港地区的攻击完败一样。因为在美国经济尚未复苏的前提下，中国经济的衰退意味着全球经济二次探底。到那时，全球央行创造的如潮货币会再次袭来，规模比2008年、2009年有过之而无不及。此时，查诺斯等对冲基金将遭遇全球政府的联合阻击。在这方面查诺斯有失败的先例，他看空美国最大的房屋信贷机构富国银行，但该银行在政府的支撑下处境还算不错。<br />
<br />
再引申一步，查诺斯对于中国经济复苏的怀疑正是建立在对全球经济复苏怀疑的基础之上，比如，他认为对于2009年在政策刺激下的汽车消费不必过多挂怀，再做多汽车股风险太大，汽车股就像航空股一样长期亏损，只会让投资者感到痛苦。<br />
<br />
如果中国继续无度地扩张信用，查诺斯的预言终究会实现。事实上，有越来越严峻的迹象显示产能扩张蕴含的巨大风险，去年12月份我国出口钢材334万吨，创出全年新高，房地产价格创出历史新高。<br />
<br />
<br />
幸运的是，除了官方部门的报喜鸟之外，有一批清醒的人士指出了信用扩张与通货膨胀的压力。事实上，中国政府意识到信贷过度与房地产泡沫的风险，近期正在紧缩信贷控制房地产行业的过度膨胀，此前一直在通过市场扩容的手段抑制证券市场指数的上涨，而近期通过推出股指期货试图一劳永逸地消解证券市场的泡沫。<br />
<br />
中国扩张的信用不足以让查诺斯盈利，但足以改变中国的经济结构与市场生态。通过2009年的信贷大扩张，新一轮的经济结构调整如火如荼，以山西煤矿重组为标志，中国的资源行业将全部集中到政府之手，进一步蔓延到农业品、房地产等领域，而此前30年培育出的商人群体日益式微。<br />
<br />
如果中国资产泡沫破灭了，那么基于资产高估与垄断溢价的国企扩张之梦也就破灭了，中国将进入另一轮市场化改革。</div>

							
							
							
							
