																											<div id="postmessage_332225" class="t_msgfont">我的朋友、南方日报记者杨大正拒贿5万元 / 傅剑峰<br />
2009-11-18 <br />
<br />
前记： <br />
<br />
昨晚深夜12时左右，杨大正忽然打电话给我，要我帮他解一下围。就是当他开给办公室桌子上的土特产礼物时，发现几根腊肠下面，全是一捆捆的百元大钞。<br />
<br />
杨大正是南方周末的前实习生，现南方日报深度报道部记者，亦是我的好友。这种事杨大正已遇到过一次，上次是拿到了一盘工商银行行长酒醉打公路收费员的录像，那名行长想以几十万元买下这盘录像，但杨大正还是把这一贿赂拒了。第二天南方日报见报，行长旋即落马。<br />
<br />
这一次，是调查东莞有关方面把垃圾偷运到惠州的事。在杨大正调查稿成稿之初，其实已经有东莞有关方面向他行贿数千元。为了稳住对方，他先假意收下，同时立即汇报分管领导，并退钱。第二天即出报。<br />
<br />
随后多天，他一直在追踪报道此事。他告诉我，昨天，老乡来说情，送了些土特色，他就觉得土特色没什么，就收下了。没想到是，这样一堆钱。<br />
<br />
这真是一位正直可爱的记者。我向他建议了处理这笔钱的办法，首先是拍照，并直接向分管领导打电话汇报，打电话务过程需录音，以证清白，防被陷害。然后继续录音，给行贿人打电话，要求对方告诉地址，把钱寄回去，这是给对方面子的一种做法。如果行贿人不愿意，那仍继续录音，给集团纪委打电话，把钱上交给纪委，同时由纪委调查行贿人。<br />
<br />
这种采访过程确实是充满凶险的，贪念一起，随时可能落入陷阱。以前就是有一名财经记者，被橡果国际用类似的方式陷害，至今还被关在监狱。<br />
<br />
杨大正在南方日报的调查报道已趋职业化，垃圾偷运一稿做得很有调查报道的风格，且职业品行方正磊落，相对于圈内若干贪婪败坏之行为，大正实可敬赞，乃是我南方风骨。<br />
<br />
没想到，今天一早看到他在开心发了贴，估计公开是为了证清白吧，也是拒其它行贿人的最有效方式。故我附记之。<br />
