烫手的5万元，钱真不是个好东西<br />
<br />
2009-11-15 00:03发表 <br />
最先关注东莞垃圾垃圾偷运的稿子是在今年8月份，因为一直没法突破，理清利益链，所以不敢下笔。<br />
<br />
写不出稿子简直比便秘还难受。一直等到10月底，终于将稿子顺利搞完。《飞来垃圾山》的调查稿发出来后，后来又做了东莞和惠州两地政府的回应。<br />
上周，又一位不愿意透露具体单位和姓名的神秘人士通过我的一位朋友兼老乡联系到我，今天中午约到绿茵阁吃饭。<br />
其实，那位神秘人士还是挺善谈的，也提出了想请我帮忙不要继续追踪垃圾偷运的事态进展，给他们压力太大了。<br />
吃完后，老乡同学将一袋子特产送给我，说是特意带给我。我当时推迟不掉也就收下了，下午出去打桌球还将那袋腊肠放在办公室。<br />
晚上吃饭了顺手将那袋特产带回家，发现还有辣鸡翅，我顺手拿出来吃。发现纸袋子下面还有一个黑色塑料袋，掏出来一看，1扎，2扎，3扎，4扎，5扎…….操，全是百元大钞。<br />
不知道一身汗是被鸡翅辣的，还是给激动的，很久没见过这么多钱了，赶快拍照。拍照完给我那个老乡电话拷问，“什么钱，我不知道啊！”妈的，你丫就装孙子吧，想害死老子！如何这袋子下午在办公室被人当垃圾扔了，我跳进黄河也洗不清了。<br />
马上致电神秘人士，那丫的也忽悠我，说小意思，一点点稿费。稿费个鸟，一点点，都抵上我半年稿费了。<br />
我让他这丫的明天滚来广州拿，这丫的说最近忙，出差，要周五才回广东。不着急，先放我这里。<br />
终于明白银行的风险多高了，我这里又没保险柜。我警告他说，限定最迟周五来拿，不来我就交给纪委就充公了。5万元可以够行贿罪起刑点了，不是看在我老乡的面子上，我直接举报你这丫的。<br />
搞晕了，晚上睡觉也要防着小偷。今天总算体会到了，钱的确不是个好东西。</div>

							
							
							
							
