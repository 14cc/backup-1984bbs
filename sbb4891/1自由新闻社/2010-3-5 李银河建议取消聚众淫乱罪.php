&nbsp; &nbsp; 目前我国刑法第六章（妨害社会管理秩序罪）第301条（聚众进行淫乱活动的，对首要分子或者多次参加的，处五年以下有期徒刑、拘役或者管制）的“聚众淫乱罪”已严重过时，建议取消。这一罪名原来被列在“流氓罪”中，流氓罪被取消后，这一罪名却保留下来，被列在刑法其他栏目中。这一罪名在目前的社会实践中已很少适用，因此建议取消。&nbsp; &nbsp;<br />
<br />
&nbsp; &nbsp; 试举几个案例（多取自最高人民检察院案例集“聚众淫乱”一章）：<br />
<br />
&nbsp; &nbsp; 个案1：被告人刘XX，女，自1981年以来，先后勾引、教唆男青年韦X等20余人，分别在这些人的住处跳低级下流的贴身舞、熄灯舞、裸体舞，舞后又主动与之乱搞两性关系，有时与多名男子进行群奸群宿。检察院以流氓罪起诉，法院以同罪对被告人作了有罪判决。(案例集，第159页)<br />
<br />
&nbsp; &nbsp; 个案2：被告人邹X等 (2女3男) 于1986年10月20日晚上到胡X住室玩朴克牌，事先规定输者让赢者亲嘴。当晚5人同睡一床，互相玩弄，群奸群宿一夜。另一晚，邹等4人 (2男2女) 又在一起玩朴克牌，为了助兴，4被告人竟先后各自脱光衣服，一男对一女，赤身裸体玩朴克牌一夜。检察院以流氓罪起诉，法院以同罪对被告人作了有罪判决。(案例集，第161页)<br />
<br />
&nbsp; &nbsp; 个案3：有一个中年流氓团伙案，经常在一个助理工程师家聚会。以下是其中一位服刑人的供述：“当爱人知道我生活不检点时，多次劝过我，周围的同志们也风言风语地刺我，而我全当成了耳旁风。心想，这顶多就是不道德，还不致于蹲监狱。终于，我们这个淫乱团伙的罪恶暴露了，我们经常在一起聚会的八个中年男女都犯了不可饶恕的流氓罪，有五个人被判了刑，那个助理工程师被枪毙了，他的老婆被判了死缓，我因流氓罪被判处有期徒刑15年。”(任克，第269页)<br />
<br />
&nbsp; &nbsp; 个案4：被告人王XX，女，先后勾引多名男子与其乱搞两性关系。检察院以流氓罪起诉，法院以同罪对被告人作了有罪判决。(案例集，第176-177页)<br />
<br />
&nbsp; &nbsp; 个案5：被告人马XX，女，19岁，伙同3名男女青年 (钟，女，16岁；李，男，15岁；贺，女，14岁) 将陈XX (男，18岁) 绑在床上，玩弄其生殖器达两个多小时。陈走后，3名女青年又用同样方法玩弄李的阴茎。后来几位女青年又做过多次类似的事。检察院以流氓罪对马XX起诉，法院以同罪对被告人作了有罪判决。(案例集，第248-249页)<br />
<br />
&nbsp; &nbsp; 上引个案是我国所有与性有关的犯罪判决中问题最大的一类。所谓“群奸群宿、聚众淫乱”不过是西方社会正常生活中屡见不鲜的“性聚会”(sex orgy)。个案3与西方70年代兴起的一种换偶活动 (swing) 有相似之处。在西方报刊的广告栏中，经常可以看到希望进行换偶活动的人寻找伴侣的广告，有时是两对夫妇相聚换偶娱乐，有时是多对夫妇进行此类活动。目前我国也存在大量此类自愿活动。<br />
<br />
&nbsp; &nbsp; 在此类活动的参与者全部是自愿参与的这一前提之下，法律绝不应当认定为有罪。因为公民对自己的身体拥有所有权，他拥有按自己的意愿使用、处置自己身体的权利。如果有人愿意在私人场所穿着衣服打扑克，他有这样做的权利；如果有人愿意在私人场所不穿衣服打扑克，他也有这样做的权利。不管在场的有几个人。国家法律干涉这种私人场所的活动，就好像当事人的身体不归当事人自己所有，而是归国家所有。如果当事人脱去衣服，损害的不是当事人自己的尊严，而是损害了国家的尊严。这种立法思想本身就是错的，错误就出在个人身体的所有权归属的问题上。在此类案件的判决中，我们应当检讨有关法律的立法思想的对错，使法律成为保护公民权利的工具，而不是伤害公民权利的工具。<br />
<br />
&nbsp; &nbsp; 此类法律不仅从个人有权利处置自己身体的人权角度看是错误的，而且从女权角度也是不可容忍的。个案4的罪名是“勾引多名男性与其乱搞两性关系”。首先，无法确知是女性勾引了男性。在两性的非婚性行为中，双方负有同等的责任，没有理由单方面判女方的罪。如果说判决的理由在于这个女人是一个人同多个男性发生性关系，从而获罪，那么又有什么证据证明和她发生性关系的那些男性全都是只有她一个性伴？如果其中的男性有除她之外的性伴，是否也应当按同罪处理？其次，一个女性有没有权利同“多名男子”发生两性关系，这是一个女性的基本权利问题。女性自愿与男性发生性关系的权利应当受到法律（宪法关于人身自由权利）的保护，而不是惩罚。<br />
<br />
&nbsp; &nbsp; 案例5的判决也十分荒唐。一群少男少女打闹调情也要判刑，令人不知身处何世。即使是中世纪宗教裁判所的严酷判官也不一定会制裁这种天真幼稚的活动。在这一判决背后，似乎有阴茎崇拜的底蕴：如果这伙年轻人在一起玩弄的不是生殖器，而是头部或脚部，大约不至于获罪，为什么玩弄一下生殖器官就要获罪呢？潜台词似乎是阴茎神圣不可侵犯。时至21世纪，一项法律还要以生殖器崇拜的逻辑来判人有罪，这不是过于荒唐和骇人听闻了吗？<br />
<br />
&nbsp; &nbsp; 对于一个“中世纪”性质的过时法律，对于一个有大量普通公民不时参与违背其规定的活动和行为的法律，对于一个在实践中实际上已经不再实行的法律，应当及时予以取消，以维护法律的权威性和严谨性。<br />
<br />
资料来源：<br />
<br />
任克(编)：《“扫黄”在一九八九》，中国人民大学出版社，1989年。<br />
<br />
