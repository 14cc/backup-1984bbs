													<h2>山东采访惊魂记</h2>
						
						
																											<div id="postmessage_194797" class="t_msgfont">我是山东人，我爱山东，但并不意味着山东有负面新闻我会主动屏蔽。<br />
跟同事驾车去德州采访。到达目的地后，采访对象的电话迟迟无法打通。<br />
后来试着去他们单位找人，仗着会说山东话，谎称找亲戚。<br />
一个小伙子还没等我把话说完，就冲出人群，急带我到僻静处，在看过证件后，带我们到一个孤僻小园，在一间小屋子里等候。<br />
原来在单位门口里的人，都是官方耳目，而那个小伙子，已经守候半天，是采访对象的手下。<br />
像特务接头，我们在一间小屋子里完成了采访，证据充足，事实基本明确。<br />
但新闻的特性是要照顾平衡，必须让你批评的人知道有人要批评他，并请他核实信息。即使他拒绝或否认。<br />
于是到政府。<br />
下午2点半了，该政府部门还没几个人来上班，前台的人说，要找X主任，得等到3点半之后。<br />
于是电话，于是请前台通告，于是等。<br />
一个小时之后，来了两个干部模样的人，自称徐局长。不请我们到办公室，却要带我上车，出去谈谈。<br />
于是到了当地最豪华的酒店，10层，已经开好一个房间。<br />
进来照例看过证件，倒水，等他们听明来意，我要求核实相关信息。<br />
开始，含糊其辞，后来，基本点头认同，看得出来，证据充足，脸上挂不住。<br />
频繁出去打电话，估计是跟宣传部长请示，无奈对方在省城，赶不过来。<br />
既然对方基本认同所采事实。遂提出离开。<br />
强行挽留，关在宾馆里不让走，先来硬的，言语中有恐吓之意。<br />
效果不佳。让我接听宣传部长电话，部长在电话那边说，你们辛苦来一趟不容易，我安排一笔经费，你们住一晚上，（估计晚上安排了捏脚、灌酒等常例），明天回京云云。<br />
当即拒绝，提出要走。对方看挽留不住，拿来两包钱，往我怀里塞。<br />
拒绝不下，佯装收着，料定不收下无法脱身，然后请他们开车送我到停车的地方。（他们开车把我拉到某酒店，同事还在车里等着）。<br />
对方提出要给我们车里加满油，才能让我们走。照例佯装答应，一切配合。<br />
在下他们的车之前，悄悄把钱留在车座上。然后迅速换到自己的车上，跟同事说赶紧走。<br />
在我们车发动的一刻，干部之一发现钱在座位上，赶紧捧着出来往车窗里塞，我手快一步，关上窗玻璃，同事明白意思，一踩油门，冲了出去。<br />
没想到，疯狂的干部们飚车赶来。<br />
我们在104国道上你追我赶，飚车几十公里。好在同事车技好，车况也好，把他们甩掉。<br />
上高速，回京。<br />
需要说明的是，我们在高速服务站加油，没用他们的油，从头到尾，我只喝了他们两口水。<br />
比较搞笑的是，其实该部门干部有一辆奥迪A6和一辆丰田霸道。但为了显示清廉，不知道从哪里搞了一辆快散架的桑坦纳2000，在追车中处于下风。<br />
