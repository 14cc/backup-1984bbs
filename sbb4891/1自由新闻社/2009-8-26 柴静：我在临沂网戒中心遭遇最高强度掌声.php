																											<div id="postmessage_216487" class="t_msgfont">柴静同学最新一期《新闻调查》做了《网瘾之戒》，内容是山东临沂杨永信使用电击法治疗所谓“网瘾”的新闻事实。很长一段时间以来，那些受过杨永信所谓“治疗”的孩子们，在百度贴吧，在天涯论坛，一次次陈述过他们所遭受的伤害。但是，无论是社会公众还是媒体都没有引起足够的关注，没有意识到电击疗法对孩子可能造成的损伤，更没有去质疑这种方法的合法性及合理性。现在，柴静和她的同事们给出了一个解答。<br />
<br />
回顾过去，“网瘾”这一概念的出现本身就含混不清。在我看来，它本身就是妖魔化网络的一种手段。成人世界和主流社会面对日益兴起和强大的网络世界无所适从，内心充满恐惧，于是这种情绪就被投射到无辜的小孩子身上，通过惩戒他们来舒缓内心的忧惧之情。如果把所谓“网瘾”视为一种精神疾病，那么许多无法理解的事情就可以理解了。如果把网人看做是“网瘾”的天然携带者，那么由于网络世界而产生的挑战就可以被理所当然地视为是一种变态。这就为针对网络世界的打压提供了医学上的借口，如同古代烧死麻风病人一样，把网络上的异类从生活中驱逐出去。<br />
<br />
类似的事情我们看到得还少吗？每一个60世代，每一个70世代，都可以回忆一下，在80和90年代的“严打”中，是不是看过高度雷同的场景？媒体大肆宣传少年犯因为黄色书籍而产生性犯罪，因为参加熄灯舞会而发生性行为，因此对传播书籍、录像带以及举办舞会的人处以严厉的惩罚。我们在今天扪心自问：现在市面上的黄色DVD的数量和烈度和二十年前相比如何？现在满街的发廊和洗浴中心和二十年前相比如何？为什么今天我们再也听不到类似的荒谬逻辑，认为淫秽物品必然诱发性犯罪？<br />
<br />
二、三十年前，成人世界面对重新焕发生机的社会心存恐惧。主流价值只承认所谓拨乱反正才是生机，夺回失去的十年才是生机。却根本没有认识到，一个社会重获生机，一定会在性的层面上猛烈复苏。性的复苏代表了人性的复苏，而性却被道统力量视为洪水猛兽。因此，残酷的打击当仁不让地落在了荷尔蒙分泌旺盛的青少年身上。如果那时候的人们也精于文辞和技术打击，那么我相信也会出现类似“性瘾”这样的名词。只不过以当时的医疗成本和管理成本，把孩子们送进监狱或者少管所代价更低，手续更为方便而已。<br />
<br />
我没有想到的是，那些曾经躲在衣橱里看《红楼梦》自渎的年轻人，那些在深夜公园里战战兢兢滚草坪的年轻人，那些因为大学里谈恋爱而被处分过的年轻人，等到他们为人父母的时候，居然毫不犹豫地把自己的孩子送进了所谓的“网瘾治疗中心”。仿佛他们在历史上遭受的压抑和打击对他们豆腐渣一样的大脑丝毫不起任何作用，以至于他们现在自己起来，把曾经加害于自身的残暴当做父母之爱，倾泻到今天的孩子身上。皇天后土，这是怎样健忘又是怎样愚昧的国民？<br />
<br />
在柴静的节目中，最可恶的并不是杨永信。作为一名优秀的演员，他成功地扮演了一位满脸微笑、极为诚恳的基层精神科医生的形象。他所表演的一切，并无推陈出新之处，和历史上所有类似的人物所做的完全一样。因此，不难理解为什么他的面前总是跪满了罹患斯德哥尔摩综合症的小朋友及其他们的家长。最可恶的是节目中出现的那一群愚昧的家长群像，面对镜头，这班人毫无廉耻和自省地表达他们这么做是出于对孩子的爱，他们的选择完全是出于无奈。甚至振振有词，觉得真理在握，居然用上了反问句。<br />
<br />
对于这些人，最大的反问句应该是这样的：光顾上操逼的时候那点爽了？孩子生下来不用教养的？数千个所谓有“网瘾”的孩子，难道都是撒旦操出来的邪童？肩负毁灭人类家庭和社会的责任？中国上网的人数过亿了，为什么偏偏你们家会出现这种怪物？这种怪物是网络教坏的，还是你们自己教化不当弄出来的？节目里没有一个家长做了自省，为自己的无能说出哪怕一个字的歉意。做家长的做失败了，把自己的失败归结为一种病，于是心安理得，甚至可以拍桌子抹眼泪，做受害者的无辜状，天底下哪里有这样推卸责任的方法？类似这样的家长，应该剥夺生殖权和抚养权。因为事实证明，他们没有行使这种权利的能力。<br />
<br />
有这样的白痴家长，所以有了杨永信的广阔市场。本来是家庭教育可以解决的问题，现在他们心甘情愿地掏每月6000元的“治疗费”，一掏就是四个半月。本来是可以及早发现及时沟通的问题，现在却无比悲情地用无证电击仪加以解决。如果时光可以倒流，我倒希望这些家长在十年二十年前被送去用电击疗法治疗他们的“性瘾”，采用这种厌恶疗法的手段彻底灭绝他们的性冲动，这样的话，现在也不用让小朋友去承受这种非人的折磨，让杨永信这种所谓的医生用去作为道具实施他的精神控制。<br />
<br />
这些家长，连同杨永信在内，都应该被送去接受一下电击治疗，去感受一下精神控制。使用电击休克疗法，我相信他们能很快接受任何观点，而且心悦诚服地爱上实施这种疗法的人。在21世纪，当文明已经达到人类会关怀一只海豹一只企鹅的生存权的时候，居然还有杨永信这样的医生，还有送孩子去找杨永信这样的家长，还有正面鼓吹网瘾治疗方法的媒体，这是人类社会的耻辱。<br />
<br />
所以，我很感谢柴静同学，以及她所有同仁的努力。一定程度上，她为所有每天上网8小时以上的人洗脱了“精神病”的罪名，使得我这样人距离杨永信的13号治疗室远一些，再远一些。<br />
<br />
而我还有一层更大的忧虑：也许今天能允许柴静制作这样的节目，并非是因为柴静她们的勇气和判断，也不是所谓“网瘾”在官方层面获得了新的定义。一切很可能只是出于现实性的考虑，我们已经看到，网络游戏所带来的利润和税收在以惊人的速度增长，网络世界所创造的价值和卷入的资金量在以惊人的速度增长。当杨永信们发挥完历史作用，开始伤害一个产业及其带来的税利时，他们的末日也就到来了。<br />
<br />
即便如此，我还是要感谢柴静。她叩下了扳机，整个世界清静了。</div>

							
							
							
							
