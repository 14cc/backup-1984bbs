转自南方都市报8月26日《深度周刊》后窗版<br />
<br />
1、眼泪<br />
<br />
&nbsp; &nbsp; 我从来没听过那样高强度的掌声。<br />
<br />
&nbsp; &nbsp; 我们在临沂网戒中心调查电击治网瘾，走进课堂的时候，所有穿迷彩服的网瘾治疗者和家长都起立鼓掌。<br />
<br />
&nbsp; &nbsp; “请第一排就坐。”杨医生对我们做了个手势，空空荡荡的第一排，名牌上写好了我们几个的名字。<br />
<br />
&nbsp; &nbsp; 我们想退到边上。<br />
<br />
&nbsp; &nbsp; 掌声骤然高起来了，杨医生笑容满面地看着我们。<br />
<br />
&nbsp; &nbsp; 这样的掌声持续了五六分钟，频率和强度没有任何变化，直到我们落座，杨医生手一挥，戛然而止。<br />
<br />
&nbsp; &nbsp; 当天的课程是点评受治者的不当表现。<br />
<br />
&nbsp; &nbsp; 一个女孩被点评的原因是她父母上报了她“跟父亲顶嘴。”<br />
<br />
&nbsp; &nbsp; 点评的内容是，杨医生问：“你父母学过心理学吗？”<br />
<br />
&nbsp; &nbsp; “没有。”<br />
<br />
&nbsp; &nbsp; “你当父母知道怎么当吗？”<br />
<br />
&nbsp; &nbsp; “不知道。”<br />
<br />
&nbsp; &nbsp; “那你要不要对你爸爸表达一下你这种愧疚的心理？”<br />
<br />
&nbsp; &nbsp; “爸爸，对不起！”<br />
<br />
&nbsp; &nbsp; “你要不要走近他面对面地对他说？”<br />
<br />
&nbsp; &nbsp; 女孩僵着。<br />
<br />
&nbsp; &nbsp; 杨医生说：“盟友们给她点勇气”<br />
<br />
&nbsp; &nbsp; 又是那种整齐划一不会停下来的掌声。<br />
<br />
&nbsp; &nbsp; 在掌声里那女孩走过去了，抱住了父亲，哭了。她的手松松地垂在父亲腰后。这段点评就这样结束了。<br />
<br />
&nbsp; &nbsp; 我采访她时，她和任何一个我在中心采访的孩子的回答都一字不差，“不怎么疼，就像针灸一样”“不超过5毫安“疼可以让人清醒“我认识到自己错了”<br />
<br />
&nbsp; &nbsp; 我打算就这样结束采访的时候，她的眼泪流下来了。我下意识地问她“你为什么痛苦？”<br />
<br />
&nbsp; &nbsp; “我没有。”<br />
<br />
&nbsp; &nbsp; “为什么哭呢？”<br />
<br />
&nbsp; &nbsp; “我没有。”她的脸很平静，声音也没有一丝抖动，只是眼泪顺着脸流下来。<br />
<br />
&nbsp; &nbsp; “你在流眼泪。”<br />
<br />
&nbsp; &nbsp; “没有。”她的眼泪已经流到腮帮上了，一大滴一大滴地落在裤子上“我愿意留在这儿”。<br />
<br />
&nbsp; &nbsp; 在这里，“挑战杨叔模式”被写在八十六条规定中，违反者会被“点现钱”――― 也就是被电击。<br />
<br />
<br />
2、跪下<br />
<br />
&nbsp; &nbsp; 这场点评课的最后场面，是杨医生问：“盟友们要怎么向父母表示一下呢？”<br />
<br />
&nbsp; &nbsp; 所有人立刻站起，奔向各自父母，搂着，下跪。他们大都躲在父母怀里大声号哭，看不清表情。母亲们一般都哭了。<br />
<br />
&nbsp; &nbsp; 然后有一个光头小伙子一个转身，向杨医生跪下，然后抱着他的腿。带着震天的哭腔喊：“谢谢杨叔！”<br />
<br />
&nbsp; &nbsp; 再然后是几十个家长和孩子都跪下了，趴在地上。<br />
<br />
&nbsp; &nbsp; 电视里这个段落没有声音，实际上，他们当时都在喊，喊的是同一句话“谢谢杨叔！”<br />
<br />
&nbsp; &nbsp; 小伙子们的头在水泥地上碰得咣咣作响。<br />
<br />
&nbsp; &nbsp; 已经第七次入院的谢乾、谢坤兄弟两人，抢在了最前面，一边一个搂住他，高声哭叫“杨叔，我对不起你……”<br />
<br />
&nbsp; &nbsp; 杨医生也搂住他们，仰脸向天，高声哭。<br />
<br />
&nbsp; &nbsp; 我以为这是一次偶然事件，后来有一次课上，听杨医生在镜头面前问：“这个中心被跪得最多的是谁？”<br />
<br />
&nbsp; &nbsp; “杨叔！”所有人都背着手坐着，整齐划一地说。<br />
<br />
&nbsp; &nbsp; “为什么要给杨叔下跪？”<br />
<br />
&nbsp; &nbsp; 我以为这类开放型问题会让大家愣一下，或者发出嘈杂的声音，但是没有，所有人的声音没有任何迟疑“感恩。”<br />
<br />
&nbsp; &nbsp; “我觉得很值，我觉得很激动。”杨医生对我说。<br />
<br />
&nbsp; &nbsp; “很多人说他们在伪装。”<br />
<br />
&nbsp; &nbsp; “这种行为能够装一辈子是不是也很好。”他说。<br />
<br />
<br />
3、恐惧<br />
<br />
&nbsp; &nbsp; 她说，去中心的当天，她儿子是被穿着三角裤，按在地下，被捆上，抬出去的，下楼的时候，所有的邻居都站在外面看着。<br />
<br />
&nbsp; &nbsp; 到了中心，他被拉进治疗室电击。<br />
<br />
&nbsp; &nbsp; “从那之后他再也不相信我了。”她说“我的心都碎了。”<br />
<br />
&nbsp; &nbsp; 但父亲很高兴，因为在中心，儿子每天给他洗袜子。这是纪律。如果违背了父母的意志，在中心，父母可以上报。<br />
<br />
&nbsp; &nbsp; 儿子和盟友蹲在地上吃一只西瓜。父亲要吃，儿子说：“你可以自己拿。”<br />
<br />
&nbsp; &nbsp; 他认为儿子不尊敬他，去上报了。第二天，儿子被电击。<br />
<br />
&nbsp; &nbsp; “后来就仇恨他。”女人低头说。<br />
<br />
&nbsp; &nbsp; 父亲说“送进去就好了。”<br />
<br />
&nbsp; &nbsp; “如果他在里面只是因为对仪器的恐惧而顺从，这是真正的改变吗？”<br />
<br />
&nbsp; &nbsp; “他要能恐惧一辈子也未必是坏事。”他说。<br />
<br />
&nbsp; &nbsp; 女人蹭地站起身，说：“不谈了，还再恐惧？再恐惧就变态了……”<br />
<br />
&nbsp; &nbsp; 她丈夫被我们劝到另一个房间后，她说，儿子拿了一把水果刀，说谁再把他送去，他就杀了自己。<br />
<br />
<br />
4、举手<br />
<br />
&nbsp; &nbsp; 课堂结束的时候，杨医生拿支话筒笑眯眯看着我。<br />
<br />
&nbsp; &nbsp; “请柴老师给我们说两句。”<br />
<br />
&nbsp; &nbsp; 我想走，但是掌声已经起来了，而且听上去永远不会停止。<br />
<br />
&nbsp; &nbsp; 立刻就有两位家长一左一右上来要搀扶我了。<br />
<br />
&nbsp; &nbsp; 最后那段现场的提问，就是在这样的环境里拍摄的。<br />
<br />
&nbsp; &nbsp; 我向家长们提问：<br />
<br />
&nbsp; &nbsp; “因为以前过于忙自己的事情而不顾及孩子的请举一下手！”<br />
<br />
&nbsp; &nbsp; “因为夫妻间关系不好而发泄在孩子身上的请举一下手！”<br />
<br />
&nbsp; &nbsp; “有过不尊重孩子的独立人格，在言语当中刺伤孩子的这样的行为经常有的，请举一下手！”<br />
<br />
&nbsp; &nbsp; ……<br />
<br />
&nbsp; &nbsp; “认为孩子是属于自己的，所以可以随意支配的，请举一下手！”<br />
<br />
&nbsp; &nbsp; 我转身向孩子。<br />
<br />
&nbsp; &nbsp; “认为自己曾经因为跟父母的关系而受到伤害，并且比较严重的，请举一下手！”<br />
<br />
&nbsp; &nbsp; “曾经在家庭当中遇到过暴力的，请举一下手！”<br />
<br />
&nbsp; &nbsp; “认为自己在家庭当中非常孤独的，请举一下手！”<br />
<br />
