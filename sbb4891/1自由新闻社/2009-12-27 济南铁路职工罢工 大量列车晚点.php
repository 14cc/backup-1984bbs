													<h2>济南铁路职工罢工 大量列车晚点</h2>
						
						
																											<div id="postmessage_400068" class="t_msgfont">(博讯北京时间2009年12月27日 转载)<br />
博讯读者爆料：“ 济南火车晚点，是因为铁路职工罢工。收入差距越拉越大，人们不能再忍。铁路局名声较好的老干部被叫去和工人谈判去了，因为现任的干部一点没威信。”<br />
<br />
济南火车站出现大面积列车晚点旅客滞留<br />
来源：齐鲁晚报<br />
□几乎涉及所有过站列车□铁路部门称原因不清楚<br />
<br />
“火车晚点情况是从下午4点左右时开始的，几乎所有经过济南站的列车都出现了晚点情况。”<br />
<br />
26日17时许，乘坐K1138次列车的庞先生发现列车在万德突然停了下来。K1138次列车是从南宁开往青岛，庞先生起初还以为列车是为了让过路车。但就这样停了一个多小时，列车仍没有开动的意思。让庞先生生气的是，乘务员只说是线路出现故障，具体也说不清什么原因，没有给一个说法。 <br />
<br />
和K1138次列车一样，D158次、K296次、K914次等多趟经停济南火车站的列车都停在了途中，原因不明。<br />
<br />
26日18时30分左右，记者在济南火车站看到，大量乘客滞留火车站，许多来接站的人员等不着人也非常着急，退票窗口处不少乘客正在退票。<br />
<br />
济南火车站值班站长曹大凡告诉记者，“火车晚点情况是从下午4点左右开始的，几乎所有经过济南站的列车都出现了晚点情况。”<br />
<br />
至于火车晚点的原因，虽然记者一再追问，曹大凡只是说：“很多列车停在线路上，具体什么原因不清楚。”站上一位工作人员则告诉记者：“可能是国家电网出现了问题，很多火车在线路上也出现了问题，现在没有准点到。”<br />
<br />
26日20时，记者向济南供电公司求证，相关负责人告诉记者，“地方上向铁路沿线供电没有问题。”<br />
<br />
记者从青岛火车站和泰山火车站了解到，这两个站也有多趟列车出现晚点情况。截至23时30分，应该22时09分到青岛的K1138次列车和22时24分到青岛的T161次列车仍未到青岛。 <br />
