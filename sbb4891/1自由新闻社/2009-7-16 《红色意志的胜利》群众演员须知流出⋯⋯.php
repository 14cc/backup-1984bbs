某学生回忆1990北京亚运：拉肚子不上厕所荣立一等功<br />
2009-02-05 14:43:27<br />
<br />
转贴：《某学生回忆1990北京亚运：拉肚子不上厕所荣立一等功》<br />
<br />
1990年秋，北京亚运会。如果有人记得那个开幕式，就会记得那个占了工人体育场东面一整面看台、破了吉尼斯纪录的超大背景台，那是三万个中学生的血肉之躯，我们学校有几百名学生参加，而我有幸在最中间的那一排，和对面主席台上的同志们大眼儿瞪大眼儿。<br />
<br />
经过一个暑假的排练，其中包括无数晒晕晒病和中途淘汰的同学（排练期间累计翻错6次则被淘汰），剩下的三万精英在工体进行八次彩排和正式开幕演出。九场表演，每场都有&quot;各个战线优秀分子&quot;来观摩。背景台的三万学生，被要求最早入场，最后退场。从头至尾一共历时九小时。每个人有一个铁架子，和双手一起，支撑着简陋而厚重的大马粪纸硬纸本（据说是北京卫戍区的解放军叔叔数个不眠之夜赶制的）。里面的四十张硬纸，乘以三万个学生，在旗语的带动下，就形成了让全亚洲眼睛亮好几个钟头的变化多端的背景台（比多哈的超大液晶屏更大、更&quot;人性化&quot;）。 <br />
<br />
为了图案上没有&quot;窟窿&quot;和&quot;缝隙&quot;，在三万学生进场以后，所有的看台出入口都被用钢架封死，上面也坐满了学生。别说上厕所，就是着火，也跑不出去。彩排以前，市、区教育领导来做关于不许拉屎撒尿的总动员，号召全体同学发扬意志品质，坚决战胜自己的屎尿。并庄严宣布，为了保持整体画面不受破坏，除了晕倒的，一律不许临阵更换后备队员。凡翻页出现错误者，轻则处分，重则取消中考资格。为了发扬&quot;人道&quot;，不让我们憋得难受，每人只发一个小方盒包装的水。八几年的广州六运会也是用的这个人工翻牌背景台，当时的错误率是万分之六，所以领导说了，这次北京亚运会的错误率要控制在万分之四以内！<br />
凡是开幕式当天出错的同学，组委会和所在区教育局视其严重程度将给予不同程度的处分，最严重的将被取消中考资格！其中几个画面是绝对不允许有错的：五星红旗、亚运会会徽、祖国山河等，哪个学校所在的位置错了，哪个学校领导负全部责任。<br />
<br />
每场九个小时。八、九月的北京，人是可以在不经意间喝掉两、三盒饮料的。先忍着渴，依依不舍地咽下最后一口水。青春期孩子都有着旺盛的新陈代谢，很快便尿意盎然，再忍尿。最后的三个小时，尿和渴一起忍，真是难得的精神历练，很有欲望弄个瓶子，尿出来再喝，做一个体外循环。 <br />
<br />
精神转移永远是最好的意志品质。饥肠辘辘、口渴难耐、尿意磅礴的我们，每个人都能倒着背出四十几个亚洲国家的出场顺序并认清每个举着不同国家代表团名字的牌子的礼仪小姐。综合来看，无疑举中国代表团牌子的礼仪小姐是当中最漂亮的…… <br />
<br />
每次开幕式彩排在闪光灯中结束。观众退场，场地里的团体操集体舞演员退场。庞大的体育场快要忘记这三万少年时，一声哨响，我们可以退场了。那时候，最羡慕坐在封堵出入口的架子上的同学。他们，是第一拨可以进厕所的人。 <br />
<br />
别以为我们都是飞奔着进入厕所的。每个人都谨小慎微地挪步，生怕步子迈大，尿了裤子，或者撕破了膀胱。进了厕所以后，就全无秩序了，没有人再去排队等候茅坑或者小便池，刚闪进厕所门，解裤子便往地上尿。心中不禁好奇，男生如此，女生们难道能优雅地排队？抓住一个女生问：&quot;我们都尿地上了。你们呢？&quot;女生吃吃地笑， &quot;进门就蹲下了，根本没看见茅坑什么样。别说我们了，班主任都尿在地上。&quot; <br />
&quot;女生蹲满一地一起哗哗&quot;被当作经典话题传颂了第一、第二场彩排，男生们都延展自己的想象力，憧憬着那种壮观。到了三四五六七八场和最后的正式表演，也就没敢多想，发觉多想这个场景，兴奋之余，也会徒增自己的尿意。 <br />
<br />
开幕式结束后两天，回到学校。校领导向大家传达市领导的活动总结。三万人中有若干因为出错而受了处分，所幸我们学校没有。领导是关怀同学们的前途的，所以没有人被取消中考资格。<br />
某中学的一个学生，（好像是171中或72中的，记不清了。）开幕式当天闹肚子，但坚持意志品质，顽强地一边拉稀，一边翻背景，用最臭的代价营造了最美的结局，正式荣立一等功。以他为中心，纵横五行以内的同学，都坚持九个小时，大闻其味儿，荣立集体三等功。<br />
<br />
据传闻，一整个暑假的排练和最后的九场开幕式，每个人每天都有90元的补贴。但是最终落实到我们手里的是：一次没翻错的同学奖励一个电子闹钟（据称市值 40元）；犯错2次以内同学的奖励一个大开本相册；3－5次的高级笔筒一支……我是得到了一个电子闹钟，在我家服役了若干年后3个针全掉了才扔掉。听说别的学校有发高级皮鞋的、有发现金的，我们这帮小毛孩子也没有去深究到底是多少奖金。部分老师还对拿了大奖高兴过头的同学给予批评：&quot;这不是为了钱，而是为祖国争光。&quot; <br />
<br />
我说的这些，恐怕也只有北京东城区和朝阳区的 91、92 届初中毕业学生还有鲜明印象。我们把那段日子，称之为&quot;亚运翻牌&quot; 。<br />
<br />
－－－－－－－－－－－－－－－－－以上是转贴，以下是纳兰的读后感－－－－－－－－－－－<br />
<br />
昨天在网上搜索疾病，不知怎么搜出这段文字。“翻牌”的事我也干过――五年级时候恭逢盛事：全市小学生运动会，我与十几个学校的几百小孩儿们一起坐在看台上，高举各色马粪纸板组成图案，腿上搁着小条:“1号正面，2号反面，3号反面......&quot;彩排了很多次，一动不动地坐一上午，极辛苦，心里还非常骄傲，但是哪有90年那些学生苦？九个九个小时，加上无数次练习，盛夏的北京，孩子的膀胱会不会憋坏落下毛病？九个小时只喝那么一点水，他们会不会因大汗淋漓而脱水中暑？万一发生火灾，为了好看而焊死的看台铁架会不会成为孩子们无法逃离的噩梦？......这些，动员学生们“不准拉屎撒尿”的领导们不知是否想过。<br />
<br />
没有哪个国家的人民有我们这个族群那样深入骨髓的“重大局”精神。在泰山压顶的“集体荣誉”和“集体利益”面前，个人的苦难、肉体折磨、生命及一切一切一切都轻如鸿毛齑粉。<br />
“大公无私”――我们的“公”很大，我们的“私”全无。我们有国家，有民族，有集体，但是就是没有自己，“自我”被鼓励认为是最先可以抛弃甚至应该抛弃的一项。<br />
<br />
词语解释：“集体主义”，是主张个人从属于社会，个人利益应当服从集团、民族、阶级和国家利益的一种思想理论，是一种精神。它的最高标准是一切言论和行动符合人民群众的集体利益，这是共产主义和无产阶级世界观的重要内容。其科学含义在于当个人利益和集体利益发生矛盾的时候要服从集体利益。一切行动和言论以集体为重个人为轻。<br />
<br />
1959－1961三年自然灾害时期，大炼钢铁和人民公社造成前所未有的饥荒，但是各村镇领导们仍不许村民外出乞讨逃荒，理由是“不能给社会主义抹黑”，于是村民们守在家中易子而食，把死去的亲人一个一个排在炕上，无力将之扛出门外落葬（其事见杨显惠《安西孤儿院纪事》）；1969年，20岁上海知青金训华奋不顾身跳进河里抢救国家电线杆牺牲，正当最好年华的青年为一根国家的木头涉险殒命；1990年北京亚运，看台上的三万学生不喝不尿，九个九小时；2008年，北京奥运会，“环保停产”：北京及周边地区所有冒烟和流污水的工厂都被勒令停工，等奥运会后再重新开工；最成功的安保：一些有信仰的“可疑分子”被政府机关没有证据和法定手续地限制自由然后劳教，无数上访者和拾荒者被清理；“邮寄限制”：液体、粉末状物和带电线的都不许邮寄。08年7月，我在北京住旅店，旅行箱里的文胸内裤等在数位眼睛贼亮的大叔的旁观之下、被店老板娘的手逐个捏摸检查一遍，回到天津，我去邮局寄一只暖手宝，因“带线儿”而被拒，旁边一个大婶好话说了一车：“我侄媳妇在广州生孩子，坐月子就想吃红糖鸡蛋，您不信拆开尝尝，就是三包红糖！”最后她抱着红糖跟我一起走出了邮局。<br />
<br />
深夜抚膺，我自认胸膛之中跳动的心脏是真切地爱着祖国的，但是为什么当我看到“广大群众牺牲小我，成就大我，万众一心，唱响集体主义的赞歌”这样的官话的时候，总会觉得有些悲凉，背脊冷浸浸的，好像有什么东西，从最开始的时候就失落了。</div>

							
							
							
							
