													<h2>释永信回应奢靡传闻：每天生活标准7块钱</h2>
						
						
																											<div id="postmessage_338687" class="t_msgfont">少林寺方丈释永信回应奢靡传闻：每天生活标准7块钱<br />
2009年11月23日 14:30中国新闻网<br />
中新网11月23日 少林寺方丈释永信23日在作客人民网访谈时表示，自己和其他僧众一样，每天的生活标准是7块钱。“工资”方面，包括自己在内，少林寺僧人每月保持一百到二百块钱的单金(工资)。<br />
<br />
释永信可以称得上是中国最具争议的寺庙方丈了，关于他的消息常常能激起网络上的热议。“少林CEO”、豪华袈裟、商业化扩张、“首开先例收受门票”、“天价开光”、“高价香”……种种争议的焦点，都直指这位名刹的方丈。日前还有人在论坛上将清苦的大悲寺僧众和风光的释永信放在一起比较，其用意自然不言自明。<br />
<br />
现实生活中的释永信是怎样的？他的生活是否真的如传言中“奢靡”？23日，作客人民网访谈的释永信回答了相关质疑，展示了僧人生活中不为人知的一面。<br />
<br />
释永信：我的生活和僧众一样 每天伙食标准7块钱<br />
<br />
释永信表示，自己的生活和僧众一样，“我从1981年到少林寺出家，近30年，我们一直保持着和僧众同吃同休的习惯”。每天自己和其他僧众一样坚持，四点起床或者五点起床，需要到禅堂上殿就要四点起床，需要到大雄宝殿上殿需要五点起床，每天和大家一起过堂吃饭。 <br />
<br />
释永信介绍说，三年前，少林寺僧众的生活标准，每天是5块钱，按照人口拨的款，拨到点若寺，由他们统一掌握，现在每天7块钱的标准。因为僧人们每天，一天三餐，完全是素食，早上晚上以粥和面条为主，中午是面条，还是北方的习惯，就是以主食为主。<br />
<br />
少林寺常住的人口，包括平时来挂单的，包括一些信徒，在寺庙用餐的，课堂里面确定人数，按照人数，由财务上把这个款拨到点若寺。点若寺根据这个人数安排伙食，每人每天的生活标准就是7块钱。 <br />
<br />
<br />
谈“工资”：少林寺僧人每月一百到二百块钱的单金<br />
<br />
少林寺僧人领不领工资？释永信每月能拿到多少钱？对于普通民众比较关注的这个问题，释永信回答说，“我们北方的寺庙相对比较注重修行”，北方还注重四修，注重修行，这一块看得很淡，每月保持一百到二百块钱的单金，“包括我在内”。其他的都是一些信徒供养的佛寺每个月正常不超过一千块钱，大概是这样一个水平。这些钱用于买一些学习用品，买点生活用品，一般僧众少不了的费用。<br />
<br />
收入稍微高一些的是哪一部分僧人？<br />
<br />
释永信介绍说，就是寺外的一些知事，包括月众。知事就是寺院里面的管理层，月众就是每天监事上殿、监事佛事，信徒供养的多一点，多也多不到哪去，差不多。 <br />
<br />
这些钱消费到什么地方？释永信回答说，这个标准在当地不错了，在当地有几百块钱的零花钱也很好了。僧人们主要用于买学习用品，比如买书、请经书或者请法器法物，或者买一个衬衣，少不了的这些费用。有时候经常做一些慈善捐款，少林寺自己有孤儿院，包括社会上的一些救灾，也做一些捐款，有很多僧人这个钱大部分用于捐出来。 <br />
<br />
<br />
释永信回应质疑：少林文化应该进入国外主流文化圈<br />
<br />
对于网络上关于其“飞来飞去，常去国外”的质疑，释永信表示，随着中国的崛起，中国文化在世界上越来越受重视。大家都在说，中国文化是世界上独一无二的文化，五千年的文化传承没有间断。少林文化又是中国文化的主要组成部分，少林寺的禅文化本身就是在交流中形成、交流中产生，在交流中发展、交流中壮大，全世界都希望了解中国文化、了解中国的禅武文化。少林寺是一个代表，所以，少林寺有责任把人类的瑰宝、优秀文化贡献给社会、贡献给人类。<br />
<br />
释永信说，大家都知道全球化，随着科技的发达，特别是近几年金融危机的发生，大家心情不稳定，都希望借助少林寺的禅武文化，平衡一下身心。因为现在只有禅武文化对身心的和谐、帮助是最有效的、最方便的。所以少林寺就到世界各地做一些交流。甚至在海外开设一些少林文化中心，就希望通过少林寺，通过少林文化，大家能够认识中国文化，能够认识中华这样一个伟大的民族。<br />
<br />
释永信说，“我们在做这个工作，收效非常好”，世界各地政要不断地来少林寺访问。外国政要也不断地邀请少林寺去参加他们的一些大型的活动。少林文化应该进入国外主流文化圈，被主流文化所接受、所认可，这是非常向往的。这一点已经做到了。在这个基础上，少林寺还会继续扩大，以及做得更好、更强。</div>

							
							
							
							
