																											<div id="postmessage_849478" class="t_msgfont"><a href="http://news.163.com/10/0929/19/6HP7CNTJ00014JB6.html" target="_blank">http://news.163.com/10/0929/19/6HP7CNTJ00014JB6.html</a><br />
<br />
2010-09-29 19:08:00　来源: 中国新闻网(北京)<br />
<br />
中新网9月29日电 据中央电视台报道，中共中央政治局29日上午就正确处理新时期人民内部矛盾问题研究进行第二十三次集体学习。<br />
<br />
中共中央总书记胡锦涛在主持学习时强调，面对复杂多变的国际形势，面对艰巨繁重的改革发展任务，我们要深刻认识正确处理人民内部矛盾的重要性和紧迫性，着眼于最大限度激发社会创造活力、最大限度增加和谐因素、最大限度减少不和谐因素，更加积极主动地处理好人民内部矛盾，为推动科学发展、促进社会和谐，为实现全面建设小康社会奋斗目标、加快推进社会主义现代化创造良好社会环境。<br />
<br />
中国社会科学院财政与贸易经济研究所高培勇教授、政治学研究所房宁研究员就这个问题进行讲解，并谈了他们的意见和建议。<br />
<br />
中共中央政治局各位同志认真听取了他们的讲解，并就有关问题进行了讨论。<br />
<br />
胡锦涛在主持学习时发表了讲话。他指出，正确处理人民内部矛盾，是关系改革发展稳定的全局性课题，是促进社会和谐的基础性工作。改革开放以来，我国经济社会发展取得巨大成就，为我们解决好人民内部矛盾奠定了坚实物质基础。同时，我们也要清醒地看到，我国正处于并将长期处于社会主义初级阶段，由于经济体制深刻变革、社会结构深刻变动、利益格局深刻调整、思想观念深刻变化，由于发展不平衡、不协调、不可持续问题短期内难以根本解决，人民内部各种具体利益矛盾难以避免地会经常地大量地表现出来。虽然人民内部矛盾是在全体人民根本利益一致基础上的矛盾，但必须高度重视，下大气力做好工作，以激发社会活力、增强人民团结，促进党和国家事业更好发展。<br />
<br />
胡锦涛强调，正确处理新时期人民内部矛盾，要以邓小平理论和“三个代表”重要思想为指导，深入贯彻落实科学发展观，强化责任，创新机制，统筹兼顾，落实措施，认真解决影响社会稳定的源头性、根本性、基础性问题，加快推进以改善民生为重点的社会建设，依法保障人民权益，不断提高正确处理人民内部矛盾能力和水平，扎实做好正确处理人民内部矛盾各项工作。<br />
<br />
胡锦涛就扎实做好正确处理人民内部矛盾工作提出4点要求。<br />
<br />
一是注重从源头上减少矛盾。要坚持聚精会神搞建设、一心一意谋发展，努力实现以人为本、全面协调可持续的科学发展，不断为正确处理人民内部矛盾打牢物质基础。要强化政府社会管理和公共服务职能，推进决策科学化、民主化，坚持把最广大人民根本利益作为制定政策、部署工作的出发点和落脚点，使各项决策符合客观实际和人民群众根本利益。<br />
<br />
二是注重维护群众权益。要健全党和政府主导的维护群众权益机制，正确反映和兼顾不同方面群众的利益，注重维护社会公平正义。要把依法治国基本方略落实到社会管理各领域和全过程，善于用法律手段解决矛盾，依法保护群众权益。<br />
<br />
三是注重做好群众工作。要认真研究和把握新形势下群众工作的特点和规律，深入做好组织群众、宣传群众、教育群众、服务群众工作，加强社会主义核心价值体系建设，加强党和国家方针政策宣传教育，推行领导干部接待群众制度，健全信访工作责任制，及时了解群众心声和实际需求。<br />
<br />
<br />
四是注重加强和创新社会管理。要主动适应社会主义市场经济条件下社会管理发展变化，积极推动社会管理体制机制创新，健全民主制度，建立健全党委领导、政府负责、社会协调、公众参与的社会管理格局，加强社会管理基层基础建设，提高基层群众自治组织自我管理、自我服务、自我教育、自我监督能力，加强社会组织管理和服务体系建设，提高社会管理信息化水平。<br />
<br />
胡锦涛强调，提高正确处理人民内部矛盾能力和水平，是加强党的执政能力建设的重要任务。各级党委和政府要把正确处理人民内部矛盾作为一项基础性工作列入重要议事日程，周密部署，扎实推进，加强调查研究，认真总结实践经验，探索新思路新办法，创新和完善体制机制。各级领导干部要自觉贯彻群众路线、切实转变作风，多做顺民意、解民忧、得民心的实事，坚决纠正损害群众利益的行为。<br />
<br />
