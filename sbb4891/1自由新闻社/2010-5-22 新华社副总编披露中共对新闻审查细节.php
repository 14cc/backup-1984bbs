<br />
1、记者假扮军人拍到汶川遇难直升机残骸<br />
<br />
这架直升机的型号是非常老旧的苏联米-171，被称为“空中拖拉机”，根本没有黑匣子。这才导致失事后很长时间都无法发现。残骸的大体位置是由民兵发现的，新华社记者李紫恒奉命前去采访。军方封锁消息，不愿意媒体披露，现场周围有军人看守。李紫恒“顺来”了一套军装，混过了盘查。现场的士兵还让他帮忙干活。到了晚上，他和随行记者为了不暴露身份，在运尸袋里过了一夜。他们徒步穿越山林，获得了第一手现场照片。他们把飞行员遗体照片发为内参，把飞机残骸照片公开发表。结果军方还得反过来朝新华社询问飞机的具体位置。<br />
<br />
<a href="http://news.xinhuanet.com/politics/2008-07/07/content_8505669_2.htm" target="_blank">http://news.xinhuanet.com/politi ... ntent_8505669_2.htm</a><br />
<br />
2、“七五”之后，没有“七七”<br />
<br />
“七五”事件是新中国最严重的民族冲突之一。在此事件中共死亡 197 人（7月20日统计），相比之下拉萨“三一四”事件死者仅有18人（年底统计）。<br />
<br />
7月8日，在外出访的胡锦涛是得到新华社的内参后，经过对形势的判断决定回国的。导致他判断事态严重性的主要依据就是新华社提供的内参。当事件从小规模冲突演变为汉族和维族寻仇的时候，民族矛盾便开始发酵。正是出于这方面的考虑，胡锦涛才决定回国全力处理此事。<br />
<br />
<a href="http://www.chinanews.com.cn/gn/news/2009/07-08/1765279.shtml" target="_blank">http://www.chinanews.com.cn/gn/news/2009/07-08/1765279.shtml</a><br />
<br />
政府在最初的应对当中，对部分维族人为主的闹事者针对汉族平民采取的虐杀采取了宽容到放任的态度。汉族认为“政府没给他撑腰”，于是自发组织了自卫性质的反抗活动，7月7日汉族人针对维族人的围殴达到高潮。<br />
<br />
需要指出的是，双方斗殴的主要参与者和对象都是底层无辜平民。那几天开私家车经过街道是没事的，但是公交车会遭到他们的主要攻击。维族人在公交车站看到人们都上车后，就跑过去拦车，选择车内人多且无法逃生的时机掀翻车辆，点火，乘客们在公交车中活活被点燃。<br />
<br />
在翻拣记者拍到的175张照片时，他看到17张就看不下去了。司法尸检显示，死者不但是被杀，简直可以说是被虐杀，他们在失去生命之外，还遭到了羞辱。甚至维族的女人和孩子也参与了斗殴，女人们领着孩子一同起哄，甚至用她们的高跟鞋踩被打倒的汉人的眼睛，还敲打头部。有孩子的脑袋被砍下来挂在街道围栏上。一个洗脚城的女孩尸体裸身被放到街上，以羞辱汉人。军方管制后，一条主要干道的路南是维族，路北是汉族，两族群众的对峙“就像巴勒斯坦的隔离墙一样”，他说。<br />
<br />
7月7日是事件发生的第三天，汉族群众约好上街，找维族人寻仇。政府发布会顾及情面，当然要说那句“没有死一个人”，但是新华社的记者们是肯定要上街的。他们绕过被安排的采访路线，钻进医院拍摄了伤者和死者的画面。<br />
<br />
事件被称为“民工打起来了”，因为参与斗殴的都是汉维两族群众中最底层的部分。照片显示，他们手里拿着棍棒，大多数光着膀子，有人身上还有刺青。他对此说道，“走上街头的人都是一无所有的人。”<br />
<br />
在反思事件出现的原因时，他这样说道：中国的社会结构现在还处于不稳定状态。不能维持稳定生活的人占了社会的多数。理想的社会应该是纺锥形的社会，中产阶级占多数。现在我们的社会是金字塔形的，底层人数是最多的。如果中产阶级，生活稳定的人们占多数的话，他们一定最不希望自己现在稳定的生活被破坏，因此也会积极主动的维护稳定的局面。<br />
<br />
“七五”事件被评论为“新疆这块和田玉上出现了不可弥补的裂痕”。因为在当时的环境下大量发布真实照片，只会进一步激化民族矛盾，导致全国汉族对维族的围攻，造成不可挽回的损失，新华社只能选择把事实报道出去的最小限度，对外发表，并刻意淡化7月7日发生的第二波冲突的重要意义。这也就是所谓“七五之后，没有七七”。实际上，当时全国27个省级行政区的防暴警察都被派往新疆维持秩序。<br />
<br />
三、满脸鲜血出舱的杨利伟<br />
<br />
当年“神舟五号”飞船成功返回，杨利伟出舱的画面经过现场直播传到全世界。画面上杨利伟尽管脸色稍显苍白，但身体状况还是良好的。他被一大群紧张的工作人员赶紧抬出舱门。但是其实这时的杨利伟已经被处理过：他是满脸鲜血地打开舱门的，后来脸上的血迹被擦干了，重拍了出舱画面。<br />
<br />
怎么会这样呢？杨利伟曾经在自己接受采访时透露了飞船升空和着陆时经历的险情。着陆时飞船必须采用升力控制技术才能避免无控制的加速坠落，就算如此航天员们也要承受数倍于地球重力的过载。杨利伟说，他当时经历的有4个G，韩国航天员经历过10个G的过载因而受伤。实际上他本人也因为重力过载而受伤了。<br />
<br />
在飞船升空时，因为设计上出现的问题，杨利伟遭遇了严重的次声波，他自己称为“共振”。次声波能引发人体器官的严重扭曲和损坏，是致命的，还被当做新型武器在发达国家研发。次声波持续了26秒，杨利伟形容“真的要以为自己要牺牲了”。<br />
<br />
<a href="http://news.ifeng.com/mil/2/201003/0326_340_1587818.shtml" target="_blank">http://news.ifeng.com/mil/2/201003/0326_340_1587818.shtml</a><br />
<br />
