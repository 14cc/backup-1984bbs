<br />
by LJin 时事评论 <br />
<br />
5月15日下午，新华社副总编、《参考新闻》编辑部主任、高级记者夏林先生到我校作报告。报告的主题是《从新华社玉树抗震救灾报道看突发新闻事件工作规律》。报告中夏林先生提供了关于汶川地震、新疆“七五”事件、“神舟五号”返回等事件中几个不为人质的细节，令人震撼。以下是本人根据现场录音整理的夏林谈话要点，可能存在不准确和模糊的地方，且未经夏林先生审阅，仅供参考。<br />
<br />
