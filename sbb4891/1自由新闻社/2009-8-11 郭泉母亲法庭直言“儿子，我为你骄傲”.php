主题: 郭泉现象研究 <br />
--------------------------------------------------------------------------------<br />
发表人: 自由生活 <br />
主题: 郭泉现象研究 <br />
发表日期: 18 十一月 2008&nbsp;&nbsp;9:21上午 <br />
--------------------------------------------------------------------------------<br />
郭泉被抓了，在网上看到这篇文章，特此转贴，以供大家讨论。 <br />
<br />
郭泉现象，是一个值得研究的课题。本人将发表一点点关于郭泉现象的看法。 <br />
<br />
1、郭泉其人 <br />
<br />
郭泉是一个青少年时期有严重自卑感的人。 <br />
<br />
1990年，郭泉毕业于南京金陵职业大学英语系。南京金陵职业大学是一所什么样的大学呢？它是一所民办大专，是中国最早打出“职业”招牌的高职教育。可以说是中国最早的大专类高职院校。 <br />
<br />
<a href="http://learning.sohu.com/2004/04/14/91/article219839133.shtml" target="_blank">http://learning.sohu.com/2004/04/14/91/article219839133.shtml</a> <br />
<br />
<br />
概况： 是一所市属全日制高等职业技术院校，是中国第一所职业大学，主要为南京经济区培养各种高级应用专门人才，实行收费、走读、择优推荐的原则。设有5个系、26个专业和1个基础课部。有正、副教授等高级技术职称者60余人。 <br />
<br />
网页显示这是2004年的情况。诸位可以设想该校的学术水平。这类高校在80年代初期开始出现，当时类似的院校有北京的中国科技经营管理大学、京桥大学、海淀走读大学。这类学校只要给钱就可以上学，是为那些考不上大学的人提供上学机会的。所以基本上可以肯定郭泉是一个高考的时候考不上大学的人。 <br />
<br />
郭泉出身于什么家庭？他父母是64年南京师大毕业生。母亲一毕业就去了省委宣传部，父亲留校，郭泉自述是在省机关宿舍和南师大校园长大的。 <br />
<br />
如此的知识分子和国家干部家庭的孩子，不能考上大学，属于比较稀罕的。至于原因是什么，郭泉似乎没有提到。后来的一些简历，他几乎不提母校的名字的，只说90年大学毕业，英文专业。实际上是双语文秘大专。 <br />
<br />
后来金陵职业大学和南京农业专科学院合并，成了高职金陵科技学院。郭泉的母校，似乎成了历史。从年龄看，他是68年出生，比大多数同年参加高考的人大一岁，从他的知识分子干部家庭来看，似乎是不该让他推迟入学的，是他有复读的原因或是留级休学的经历，我们不得而知。 <br />
<br />
郭泉毕业的时候是三年制大专。根据他自述，89年64他是大学2年级，90年毕业，就是说他的学制是从87年9月到1990年7月。他肯定没有大学本科学历，也没有学士学位。但是1996年他获得了南京大学的法学硕士学位。这说明他应该是1993年入学的研究生。以他的学历和水平，如何考得上南京大学的研究生并获得硕士学位，是个很大的疑问。因为从郭泉的文字里，我看不出他的法学修养。 <br />
<br />
1999年郭泉获南京大学哲学博士学位。专业方向是中国古代佛学。考上南京大学博士的研究生并不是一件容易的事情。郭泉即使在获得博士学位以后，也没有任何文章显示他有足够的佛学造诣。因此他如何被南京大学哲学系录取为博士研究生，也是令人怀疑的。 <br />
<br />
1999年至2001年郭泉在南京师范大学做文艺学博士后研究；2001年博士后出站之后留校，担任副教授、硕士生导师至今。 <br />
<br />
从郭泉的学历和工作经历，我们看不出为什么南京师范大学要请他做文艺学的博士后。从郭泉的文章看，我们看不出郭泉有文艺学的修养。 <br />
<br />
所以郭泉的学历，展示了一个非常神奇的现象。郭泉的职业，也很神奇： <br />
<br />
他以民办自费走读学校大专生的身份，变成国企领导，变成南京市政府经济体制改革委员会秘书（这可是大肥缺），又变成了法院的书记员，拿到了审判员资格，还拿到了南京大学法学硕士学位。他自述有5年的法律经验，就是说在1990年到1996年之间有5年时间在法院工作，就是说他拿到大专文凭以后马上就在一年之内担任国企领导和南京市体改办秘书，在中国，需要多大的后台和关系网，才能得到这样的肥缺？ <br />
<br />
从郭泉的性格看，他并不是一个韦小宝那样擅长观颜查色、左右逢源的官场奇才，他也缺乏陈水扁那样的学术天赋足以让他从从底层奋斗到博士后副教授。 <br />
<br />
一种可能的解释是，郭泉一帆风顺的背后，实际上有他家族的功劳。虽然我们不知道他的家族详情，但是可以猜想到至少有一个人是在江苏省和教委系统有影响力的。 <br />
<br />
这就带给郭泉一个悲剧――他的成功不完全是自己挣来的，他始终缺乏成就感，这种自卑感带给他的，是奋发图强，是做一番大事业给人们看看，以此证明郭泉不是个废物。 <br />
<br />
郭泉在2005年砸王直的坟墓之前并没有什么壮举。他一帆风顺在国企和政府部门、体制内高校混得不错，说明他并那些年并没有什么桀骜不驯的性格表露。那时候他是体制内的人。 <br />
<br />
但是几年的副教授生涯，没有显示郭泉是一个特有天赋搞学问出人头地的人，没有显示副教授的事业足以抵消他的自卑感，或者说，这份在他人看来很不错的工作离他垂名青史的雄心壮志相差甚远。所以郭泉决心做一番大事业，要成为一个轰轰烈烈的人物，让自己可以逃避潜意识中自卑感的折磨 <br />
<br />
（补充：有人告诉我说：郭泉是靠实力考上硕士的，博士能够录取是因为方向冷门，另外，这位网友告诉我：郭泉进入市委体改办工作也是考试进入的。对此，如果我有误判的地方，或者有更充分的证据显示郭泉在考试的过程中没有动用任何社会关系得到优惠照顾，本人将为自己的错误猜测道歉。在有充分的证据推翻我的猜测之前，本人保持文章原样。） <br />
<br />
<br />
1995年郭泉在南京的莫愁路教堂受洗入教，并自称一直致力于儒释道三学和基督教的结合研究和教学。 <br />
<br />
但是，我们从郭泉的文章里，读不到基督徒的风格。基督徒要求的诚实、谦卑、荣耀归救主，郭泉没有。基督教对生命的珍惜，郭泉没有。我想任何一个基督徒不会赞扬郭泉宣扬的“无限防卫”――在小偷亮刀威胁的时候乘机把小偷打死，因为这样可以打死小偷而不负法律责任。我想也没有很多法学家会公开赞同有5年法律工作经验的法学硕士郭泉的“无限防卫”。 <br />
<br />
至于他后来没有去研究神学，却研究佛学思想，这也和一般基督徒的思路不符。 <br />
<br />
我猜想他之所以成为一个基督徒，纯粹是处于内心的苦闷和无所依赖。或者甚至可能是某些亲友出于“治病”的目的建议他加入基督教。郭泉的心理一直不是健康的，可能在95年前后或更早的时间就出现过心理障碍。 <br />
<br />
至于他博士研究佛学，大概是因为基督教的思想并没能解脱他的心灵痛苦，转而希望从另外的角度寻求解脱。如果他真的如他自己所诉，是一个20年来一直追求民主事业的人，他似乎更应该继续研究法学或者政治学、经济学、社会学才对，而不是去研究佛学和文艺学。 <br />
<br />
（补充：以上关于郭泉转向隋唐佛学思想方向的原因，是本人猜测错误，因为据友人说是因为该专业冷门，没人报考。） <br />
<br />
以上提到的郭泉说自己为今天准备了将近20年（从89开始），是某个民愤朋友说郭泉亲自在网聊的时候说的。但是公开场合郭泉是否这么说呢？郭泉的一篇文章是这么写的： <br />
<br />
“89年，我在南京金陵职业大学（现名南京科技学院）的文法系中英文秘书专业上大学二年级。那年，所有的大学生都激烈地反对“官倒”、反对腐败，要求中共结束独裁，实现民主。百万大学生在全国各城市的主要街道开始了全世界有史以来规模最大的民主运动。我们在街头向每一个过路的市民介绍民主思想，恨不得让所有人都支持我们。 <br />
<br />
但是，当时的工人、农民很奇怪我们在做的事情。一些工人说：“小祖宗哎，你们让些路给我们吧，我们要去上班，迟到要扣奖金的。”全国的农民也都不知道我们这些十几岁、二十岁的娃娃们在折腾个啥。 <br />
<br />
最后，中共以“人民不答应”的理由，镇压了我们这些大学生的美好的心灵和年轻的身体。93年，我到南京大学读社会学（法学）研究生，一些苦难的工农到南京大学找到我，请我帮他们维权，我没好气的说：“我不干！当我们在街头最需要你们工农帮助的时候，你们却在奚落我们。现在你们知道我们当时在做什么了吧！你们现在活该！”96年，又有军转干部来找我维权，我更愤怒了，说：“你们枪杀我的好兄弟，你们碾压我的好姐妹，你们是杀人犯，去死吧你们！” <br />
<br />
但是，2002年6月4日晚，我梦见一位牺牲在北京的女同学，她对我说：“蝈蝈！你傻呀，你应该帮他们，我们死这么多人，不就是为了让人民得幸福吗？” <br />
<br />
当我醒来的时候，我就从一个June Fourth学生变成了一个维权民运人士了。之后我做的维权的事情大家也都知道了。这里就不再赘述了。” <br />
<br />
这种神奇的故事，总是容易发生在一些心理不太正常的人身上。有个叫张国堂的基督徒，在大街上行走的时候被圣灵充满，醒来发现他就是救世主，肩负拯救中国的命运。郭泉的自述似乎也有点张国堂的风格。只是他为什么一开始并不是为工农和退伍军人维权，却是一个积极的反日人士呢？等到他想到维权的时候，实际上是他公开反悔自己反日行为的时候。网上流传一段qq聊天记录，记载郭泉对砸墓而感到懊恼。 <br />
<br />
但是，所谓2002年他决定为工农和退役军人维权的故事，以及那个刚好发生在June Fourth晚上的梦，就很值得怀疑。至于他开汉服店宣传汉服运动，开武术馆宣传无限防卫，都让人不愿意相信郭泉既是一个神志清醒的人又是诚实的理想主义者。 <br />
<br />
事实上，郭泉真正公开从事维权运动，是从2007年开始的。 <br />
<br />
前几天有人爆料，说郭泉因玩弄女学生导致2位女生实名举报，郭泉被南师大保卫处带走调查。对此事某作家X舰X向有关部门求证，据说是真实的。又有南师大学生爆料说女生家长找到学校来。 <br />
<br />
对于这些八卦新闻，我并不在意。我认为一个男人有几个情人是他自己的自由。当然，如果是利用教授的职位对任课的或者有相关利益的下属实行胁迫，那是索取性贿赂或者变相的强奸，那是另一个问题。虽然有人宣称郭泉曾经用女生的成绩要挟，但是没有足够的证据。我也怀疑这种说法是造谣，因为郭泉老师一向讨好学生，没有证据显示他用分数卡学生。有一个南师大的男生说，他选修郭老师的一门课，开学的时候听了半节课，后来就没去，连考试都没参加，拿到成绩一看，93！所以我认为郭泉用成绩胁迫女生的说法与郭的教学风格不符合。 <br />
<br />
有人认为制造郭老师的绯闻是五毛造谣，为抓捕郭泉做准备，这似乎也不大合理。因为关于郭老师的八卦绯闻，在郭老师还是反日愤青的时候就已经有传说。我认为是即使是有绯闻，也多半是女生对老师的倾慕，这算不得丑闻，甚至可以认为是魅力的象征。 <br />
<br />
并不是每个大学女生都象我这样能够冷眼看英雄。美人爱英雄，或者美人爱才子，似乎是自古以来就不变的天理。郭老师的文笔才情虽然很不才子，但是他特立独行的作风在很多学生眼里，确实够英雄。 <br />
<br />
我认为郭老师的一些婚外恋故事是真实的，正如本人的一些网恋故事一样，未必需要虚构。有些男人天生风流，有些男人性格多情，有些男人本性好色，郭老师的风格，似乎是需要吸引更多的异性证明自己，以此摆脱自幼以来根深蒂固的自卑感。 <br />
<br />
<br />
郭泉的很多文章并非自己原创，而是他人网文的改头换面。他原创的思想很少，甚至也没有深刻的来自别人的思想。如果是一个严格的学者，他引用别人的文章会指出出处，郭副教授似乎没有这个习惯。 <br />
<br />
把别人的思想和文章改头换面为《民主先声》系列，并非学术正道。但作为政治操作来说，无可厚非。我们不能要求一个政治家不剽窃。 <br />
<br />
但是，即使单单从《民主先声》来说，似乎也看到很多矛盾点。郭泉宣扬民主自由，要求政府给百姓极大的自由，但是他又宣扬毛泽东式的全民福利，扬言要来一次无产阶级革命推翻官僚统治的XX党。他的文章和一些聊天记录显示他提倡政府包养人民的思想。这种矛盾在自由主义这看来是明显的，因为自由主义者反对政府过分扩大它的权力范围，但是郭泉似乎丝毫没有感到不妥。对于这全民福利的钱的来源，郭泉未说明他是如何解决的，或许在他看来，只要推翻了代表官僚阶级的XX党，让代表无产阶级的政党被人民选上台，把被官僚阶级掠夺的钱分回到人民手中，经济问题就自行解决了。 <br />
<br />
郭泉反对某政党擅自代表大众，但是他又成立了三个党代表几乎所有的底层百姓，自称党魁，这的那个然是没有任何授权的。郭泉反对某党撒谎吹牛欺骗大众，但是他自己吹嘘的新民党，先从800万，到3000万，到7000万，后来又更正为1000万党员。他自称基督教民主党是一些家庭教会创办的，但是家庭教会是否把他看做一个正信的基督徒都是个问题，或者说在一般的基督徒看来，他根本就不是个正信的基督徒。 <br />
<br />
郭泉的《民主先声》系列文章不具备一个学者应该有的周密性。 <br />
<br />
甚至可以说，是一个理智不健全的人，或者说心理有问题的人，甚至是一个精神病患者的风格。 <br />
<br />
<br />
我对于郭泉其人的看法是：为了摆脱自卑感造成的心理压力，他不择手段地要成名建功。无论是汉服运动，反日运动，无限防卫运动，维权反独裁运动，都是出于一种心理障碍造成的发泄，而不是某些人所误会的理想主义者的英勇抗争。 <br />
<br />
郭泉不是一个真正的理想主义者。一个真正的理想主义者，会不屑用自己的家族力量去获取他的学位和工作机会，会不屑剽窃他人的思想和文字，会不屑靠吹牛去炫耀自己。 <br />
<br />
但是他又是一个很有自尊心的人，这点自尊心要求他摆脱靠家族成功的阴影，以及本职工作上毫无建树的阴影，从而渴望成为一举成名的大英雄。 <br />
<br />
郭泉成为一个英雄，不是一个理性的选择，不是一个智者的选择，而是一个有严重心理障碍的loser寻求自我肯定的一种选择。 <br />
<br />
这就是郭泉其人。 <br />
<br />
<br />
2、郭泉粉丝 <br />
<br />
郭泉的粉丝基本上是一些很年轻很有正义感的人。 <br />
<br />
本人是一个对正义感很不在意的人。我的豚儒主义，就是要解构正义感的。所谓正义感，就是为自己找到一个理由，让自己感到舒服。 <br />
<br />
郭泉的粉丝对社会不满，这点和我没有区别。他们希望改造社会，建立一个更好的社会，这点也跟我相同。 <br />
<br />
但是，他们和我的区别是：他们是一群容易被光环效应迷惑的人。他们拒绝站在纯粹旁观的立场上去冷眼看英雄。 <br />
<br />
他们未必认为郭泉所作的都是对的，有些人固然是反日愤青，但是更多的人非常反对郭泉的砸墓活动，极度蔑视反日愤青。他们中间，右愤比左愤多，当然这里的左右是按照中国人的习惯说法，而不是正统的或学术的说法。 <br />
<br />
他们喜欢如此反驳我：他为了民主事业，不顾一切，放弃了优厚的生活，背叛了他的特权阶层，难道这样的英雄不值得敬佩吗？ <br />
<br />
饱醉豚的答复是：佩服或不佩服一个人，纯属个人选择。你佩服他，我不喜欢他，都是个人自由。我讨厌任何人以为他佩服的人我就得跟他一起佩服。 <br />
<br />
<br />
如果郭泉是因为男女问题被起诉，因为诈骗钱财被起诉，因为教唆罪被起诉，诸位粉丝大概会觉得这是一个“政治问题非政治化的典型”。但是很荣幸，中共政府以“颠覆国家政权”的行为抓他。这对郭泉是一件大好事，等于要成就他的名声。这是给郭泉他日思夜想的桂冠。 <br />
<br />
我和郭泉粉丝的区别在于：我习惯把一个人或一件事从多方面细分开来，一个个方面去想，而不会把一个模糊的整体的概念全盘拿来用。当你说郭泉的时候，会把很多郭泉的事件连在一起，因为“反政府英雄”的光环是在太过于强烈，以至于粉丝们很少能够深入去思考郭泉到底是怎样的一个人，他为什么要这样做。他们觉得，一个人只要因为“颠覆政府”被起诉，这个人就是个值得崇拜的大英雄，而忽略其他的方面。 <br />
<br />
郭泉的粉丝会注意到他是个跨越多个学科的副教授，但不会去仔细阅读郭泉的文章以求发现郭泉在各个学科的实力。他们会注意到他的博士学位和博士后经历，但是不会找到他的母校金陵职业大学。他们看到郭泉丰富的工作经历，却不去思考为什么一个考不上公费大学的自费大专生何以能够成为市委体改办的秘书和法院的工作人员。 <br />
<br />
所以，他们不能发现他们崇拜的大英雄实际上是一个被自卑折磨多年的心理障碍者。 <br />
<br />
所以，他们不能发现这个大英雄所作所为实际上是一种试图证明自己的自我心理治疗。 <br />
<br />
而他们更没有能力把一个真正的理想主义者和郭泉这样饥不择食的投机者区分开来。 <br />
<br />
郭泉的粉丝大多数不喜欢严密的思维。他们不能从郭泉的文章和谈话种发现他其实是一个极度矛盾的人，他的政治思想不仅不成熟，甚至可以说没有自己的政治思想。他能做的，是把异议群体的声音拷贝到他的《民主先声》，郭泉是一个编辑或出版者，而不是一个思想家或启蒙者。 <br />
<br />
郭泉是一个急于成名、急于建功立业的人。以至于到了不择手段的程度。他善于鼓动民粹，或者他一直鼓动民粹。他的立场一直在变，从极端民族主义者变成维权人士。但是实际上他又没有变，他一直在寻找那个能让他一呼百应的东西。他追求的是一种事业的成就感，而不是真正的信念。所以我说他是一个为了为了建功立业不择手段的人，只要能够成为大英雄，他可以很快改变立场，改变思想。 <br />
<br />
正因为这个原因，郭泉的粉丝从早期的以极端民族主义者为主转变为以民主右愤为主，从那些信奉暴力的“无限防卫”青少年到那些忧国忧民关注人权的“民主人士”。这种变化是如此明显，但是很多粉丝似乎并未注意到。 <br />
<br />
有些人表面上是郭泉的粉丝，但实际上不是。 <br />
<br />
比如说一些海外的民运，一个个声援郭泉特积极。如果你以为他们是郭泉的粉丝或同志，那就大错特错。 <br />
<br />
很多海外老民运生活艰难，他们需要搞一些活动，才有某些人给他们生活费搞民运。但是并非总是有很多机会搞活动的。国内有几个郭泉这样的人，对他们是大好事，可以乘机发起签名信公开信，可以写文章声援郭泉，可以到中国大使馆门口抗议，这样就显得他们是在搞活动，是在为中国的民主事业奔波，而不是一群老废物。这些人是郭泉的粉丝吗？显然不是。 <br />
<br />
比如说，一些海外假民运，实际上是做非法移民生意的，他们帮那些坐集装箱偷渡的福建偷渡客申请政治庇护，一个人收费6000-9000美金，郭泉被抓了，抗议活动最有声有色的肯定是他们，因为那些非法移民只有拼命表演抗议中国政府才能拿到政治庇护，否则钱就白交了，当然，他们参加抗议活动是需要交费给XX反对党的头子的。这些人拿到了绿卡以后，会马上跟中国大使馆套近乎，成为“爱国华侨”。因为党也深深知道，他们要的是北美绿卡，而不是民主自由。 <br />
<br />
还有一些国内签名的异议人士，他们都是郭泉的粉丝吗？当然不是，他们需要的是借郭泉事件掀起一次又一次废除刑法第105条活动。 <br />
<br />
所以，并非郭泉的支持者就是郭泉的粉丝。有些是利用郭泉给自己打声势，有些根本把郭泉当炮灰利用。 <br />
<br />
<br />
3、郭泉现象 <br />
<br />
郭泉这样神志不清的人成为一个举世瞩目的英雄，我称之为郭泉现象。 <br />
<br />
郭泉现象的条件是：大众的期望被过度压抑的时候，哪怕一个很可怜的代替物，也能满足大众的欲望。 <br />
<br />
毛主席说过一个例子：怎样让马和驴子交配生骡子？只要把马和马放在一起调情，驴子和驴子放一起调情，然后蒙上眼睛把他们分开重组，性欲发作的马和驴子就交配了。 <br />
<br />
现在政府严厉限制了组党之类的活动，严禁封锁信息，就是蒙上大众的眼睛。大众就是被社会现实启蒙得欲火中烧的马，这时候看到了郭泉这样的驴子，就难免要去满足一下。 <br />
<br />
如果中国是一个开放的社会，如果组党自由，如果各派都可以自由表达自己的主张并接受大众的挑选，郭泉这样的人在政治舞台上几乎就是一个芙蓉姐姐般的笑料。但是在没有舞台表演自由的时候，哪怕跳到舞台上的只有芙蓉姐姐，她也是大众唯一可以欣赏的明星。 <br />
<br />
郭泉其实得到了他所希望的东西。他寻求出名，寻求建功立业，寻求成为大英雄。他现在被抓了，终于满足了一把，似乎可以摆脱心理自卑的阴影了。 <br />
<br />
中国政府也得到了希望的东西。它把驴子和马交配，生下骡子。民运英雄的形象也因此和郭泉这样的2B连在一起，于是达到丑化民运人士的目的，为安定团结做出不少政绩。 <br />
<br />
大众得到了什么？他们爽了一把，hi了一把。他们以后会把郭泉这样的人当作偶像，那些不够hi的民运活动已经不能勾引大众的性欲，因为有了郭泉，大众的民主性阈值增高了。 <br />
<br />
这是一个皆大欢喜的结局。郭泉播下的星星之火能够燎原吗？人们能够突破组党的恐惧吗？ <br />
<br />
我只是担心，有一天政府扔出一个郭泉的认罪书或忏悔书，而这个忏悔书确实是郭泉写的。那时候希望大众挺住，不灰心啊。郭泉不过是郭泉，仅此而已。 <br />
<br />
<br />
4、一些不同意见 <br />
<br />
有些人不同意我的意见，也不同意大众把郭泉的行为当作实质上的英雄行为。他们的观点是：郭泉是一个真正清醒的民运人士，他的佯狂是一种行为艺术，一种迷惑当局或者让当局无从下手的手段。只有通过这种类似与芙蓉姐姐的政治运动表演，搞笑的组党，夹杂一些真正的政治诉求，这样才能不断冲击政府的底线，消除大众对政治恐怖的恐惧心理。 <br />
<br />
我完全理解这种人的观点。我也很赞赏这种政治行为艺术，认为这是一种非常好的方式。我确信郭泉真有这样佯狂装名士的心理。问题是：郭泉不具备干这个的心理素质。当他佯狂的时候，他进入了角色而无法从中解脱出来。心理学上有一个著名的监狱实验――把一群志愿者弄到监狱去表演狱卒和囚犯等角色，大家很快进入了角色，囚犯真感到自己是囚犯，甚至出现了严重的心理疾病。 <br />
<br />
郭泉就是一个自以为在演戏却把自己忽悠进去的民运英雄。他已经不能从中解脱出来。因为这个戏太符合他的心理渴求了。张铁林说，等到众人跪下来山呼万岁，你就找到当皇帝的感觉了。 <br />
<br />
我很想问候一声狱中的郭泉： 郭老师，您现在觉得自己是英雄了吗？ <br />
<br />
我想问一声郭泉的粉丝：你们真觉得郭泉不是在演戏吗？郭泉，实际上是被你们忽悠到监狱里面去的。你们不知道他在演戏，你们不知道他在给自己治病。你们配合他演戏，正如一群人跪在张铁林身前山呼万岁。而张铁林要是不幸有郭泉那样的心理障碍，可能宣布自己要当中国总统了。<br />
<br />
