&nbsp; &nbsp;一些郭泉的粉丝、非粉丝以及郭泉的学生<br />
<br />
<br />
&nbsp; &nbsp;<br />
<br />
&nbsp; &nbsp;在网上遇到郭泉的粉丝，通常是在qq群里。我先后被人拉扯到至少二十几个时政qq群，几乎每一个qq群都有郭泉的粉丝到处转贴郭泉的《民主先声》系列，有时一天会贴好多次。数年来本人对郭泉的蔑视从来不曾有过丝毫改变，忍不住嘲笑郭泉几句，郭泉的粉丝会质问我，最典型的对话是这样的：<br />
<br />
&nbsp; &nbsp;<br />
<br />
&nbsp; &nbsp;郭粉：<br />
<br />
&nbsp; &nbsp;你居然骂郭泉!<br />
<br />
&nbsp; &nbsp;<br />
<br />
&nbsp; &nbsp;饱醉豚：<br />
<br />
&nbsp; &nbsp;这种混蛋骗子，当然要骂。<br />
<br />
&nbsp; &nbsp;<br />
<br />
&nbsp; &nbsp;郭粉：<br />
<br />
&nbsp; &nbsp;他骗你钱还是骗走你老婆？<br />
<br />
&nbsp; &nbsp;<br />
<br />
&nbsp; &nbsp;饱醉豚：<br />
<br />
&nbsp; &nbsp;那些没骗走你老婆没骗走你钱的人就不是骗子了？<br />
<br />
&nbsp; &nbsp;<br />
<br />
&nbsp; &nbsp;郭粉：<br />
<br />
&nbsp; &nbsp;共产党骗你，抓你，关你，压迫你，你不骂；郭泉为了你的自由和人权去抗争，你倒骂郭泉！<br />
<br />
&nbsp; &nbsp;<br />
<br />
&nbsp; &nbsp;饱醉豚：<br />
<br />
&nbsp; &nbsp;共产党没有抓我关我，也骗不了我。我也没授权郭泉替我去抗争。<br />
<br />
&nbsp; &nbsp;<br />
<br />
&nbsp; &nbsp;郭粉：<br />
<br />
&nbsp; &nbsp;难道郭泉不是为了中国人民的民主自由去战斗？难道他不是你我的权利去斗争吗？<br />
<br />
&nbsp; &nbsp;<br />
<br />
&nbsp; &nbsp;饱醉豚：<br />
<br />
&nbsp; &nbsp;丫就是一个想出名想疯了的神经病投机装逼犯，后面跟一大群更傻逼的神经病。<br />
<br />
&nbsp; &nbsp;<br />
<br />
&nbsp; &nbsp;郭粉：<br />
<br />
&nbsp; &nbsp;郭泉放着教授的职位不干，被土共拘捕好几次，郭泉放着高薪收入不要，为弱势群体呼吁，世界上有这样的投机骗子吗？<br />
<br />
&nbsp; &nbsp;<br />
<br />
&nbsp; &nbsp;饱醉豚：<br />
<br />
&nbsp; &nbsp;他不是得到了他需要的名声吗？你不就受骗上当了吗？<br />
<br />
&nbsp; &nbsp;<br />
<br />
&nbsp; &nbsp;郭粉：<br />
<br />
&nbsp; &nbsp;你党妈给了你多少钱让你骂郭泉？<br />
<br />
&nbsp; &nbsp;<br />
<br />
&nbsp; &nbsp;饱醉豚：<br />
<br />
&nbsp; &nbsp;我妈不是共产党员。郭泉说他妈才是共产党员。<br />
<br />
&nbsp; &nbsp;<br />
<br />
&nbsp; &nbsp;郭粉：<br />
<br />
&nbsp; &nbsp;操你妈逼五毛！<br />
<br />
&nbsp; &nbsp;<br />
<br />
&nbsp; &nbsp;以上这段风格的对话我经历过多次，是典型的郭粉风格。我开始几次还以为是同一个人用不同的马甲到处转贴，后来才知道并非同一个人，而是风格类似的一类人。<br />
<br />
&nbsp; &nbsp;<br />
<br />
&nbsp; &nbsp;还有一种人自称不是郭粉，也不认可郭泉的自吹自擂。但是我骂郭泉的时候，他们也会愤怒。通常的qq对话是这样的：<br />
<br />
&nbsp; &nbsp;<br />
<br />
&nbsp; &nbsp;非郭粉：<br />
<br />
&nbsp; &nbsp;我也不认可郭泉的一些所作所为，我也和你一样讨厌他砸坟墓反日货，但是郭泉是确确实实用实际行动争取自由民主的人。<br />
<br />
&nbsp; &nbsp;<br />
<br />
&nbsp; &nbsp;饱醉豚：<br />
<br />
&nbsp; &nbsp;我认为他所作所为实际上起到抹黑民运的作用。<br />
<br />
&nbsp; &nbsp;<br />
<br />
&nbsp; &nbsp;非郭粉：<br />
<br />
&nbsp; &nbsp;但是那些所谓的民运，他们做了些什么？他们只会躲在国外指手画脚，只会拍主子的马屁，真正干事的是郭泉。<br />
<br />
&nbsp; &nbsp;<br />
<br />
&nbsp; &nbsp;饱醉豚：<br />
<br />
&nbsp; &nbsp;你真认为郭泉是为民主自由而献身的理想主义者？而不是一个想出名想疯了的人？<br />
<br />
&nbsp; &nbsp;<br />
<br />
&nbsp; &nbsp;非郭粉：<br />
<br />
&nbsp; &nbsp;我承认郭泉是一个很想出名的人，我也相信网上的一些关于郭泉人品败坏的传言是真的，但是只要是反共的，只要是争取自由民主的，我们都理所应当支持他。<br />
<br />
&nbsp; &nbsp;<br />
<br />
&nbsp; &nbsp;饱醉豚：<br />
<br />
&nbsp; &nbsp;我不喜欢用政治立场掩盖品行恶劣的人。郭泉是一个没有立场没有底线的人，是一个为了出名随时可以改变立场的人。<br />
<br />
&nbsp; &nbsp;<br />
<br />
&nbsp; &nbsp;非郭粉：<br />
<br />
&nbsp; &nbsp;但是民主大业需要这样的人，谁的立场不会改变呢？任何人的立场都会改变，只要对我们民主事业有利的，不管多么坏的人，我们也要支持，只要他的行为实际上对民主自由起到了促进作用。<br />
<br />
&nbsp; &nbsp;<br />
<br />
&nbsp; &nbsp;我和这类非郭粉也是没有共同语言的。他们是一群信仰民主的人，只要为了民主大业，手段或人品可以摆在次要的位置。而饱醉豚并非政治狂热分子，更不是民主教的信徒。我看重人品超过其他的，人品是一种相对稳定的东西，而观点和立场是一种变化太快的东西。那些本来没什么观点和立场，为了出名获利而迅速变化观点和立场的人，也就是鲁迅所说的流氓，大概就是郭泉这种人。郭泉这类人的存在让人没有安全感，因为他和他的粉丝都是充满了暴力欲望的自以为懂得什么是自由的拳匪。<br />
<br />
&nbsp; &nbsp;<br />
<br />
&nbsp; &nbsp;我也反对为了崇高的理想不择手段。正义这东西很靠不住，崇高这东西更靠不住。我一直觉得，二战时期的日本军人和德国军人也是充满崇高和正义的感觉，一定比中国军人更崇高更正义，否则他们怎么会这么勇敢这么不怕死呢。<br />
<br />
&nbsp; &nbsp;<br />
<br />
&nbsp; &nbsp;三天前，郭泉的一个反对者，南京师大文学院毕业的，看到我的《郭泉现象研究》，跑到qq群里抖出郭泉的不少轶事丑闻。他不仅反对郭泉的人品，也反对郭泉的政治活动。他说：社会需要稳定，比如我们这样的人，只要社会稳定，大家都可以有足够的收入和不错的前途，一旦社会动乱，这些美好的生活就会丧失。所以他特讨厌郭泉煽动学生反政府。他认为一个强势的政府是需要的，只有一个具有权威的强大政府，才能保证社会的稳定，再能有美好的生活。而现在的共产党政府基本上符合他的理想。这种观点牛博网的王昊轩和青年墨水风格。或许在牛博网的读者看来，持这种观点是很可怜的，但是放眼看看今日的大学生，大多数是这种观点，或者说他们才是主流。<br />
<br />
&nbsp; &nbsp;<br />
<br />
&nbsp; &nbsp;跟郭粉、非郭粉、王昊轩等人相比，本人的社会责任感要低一些。我不觉得自己需要为社会承担那么多使命，政治的事情，是政治家的责任。廖亦武老师有一句名言：“老子不搞政治，再心理变态也不会祸国殃民”，但是廖亦武对那些有心理变态的或者骗人成瘾的政治投机分子也是要破口大骂的，比如那篇在盛宴上放老鼠药的文章。而郭泉，正是变态心理搞政治的典型。</div>

							
							
							
							
