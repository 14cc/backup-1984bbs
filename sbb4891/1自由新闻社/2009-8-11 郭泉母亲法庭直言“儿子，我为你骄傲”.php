<br />
<br />
&nbsp; &nbsp; 中华人民共和国公民应该有宪法保障的言论、出版、集会、结社、游行、示威的自由。我们应该维护宪法，维护它的每一条，维护它赋予每一个公民的正当权益，维护民主与法制。<br />
<br />
<br />
2，郭泉没有非法组织政党的行为。<br />
<br />
<br />
起诉书指控郭泉非法组织政党，那么，请问，我们国家有合法组织政党的法律、法规及实施细则规定的方式吗？如果郭泉想组党，可以去哪里合法申请组织一个政党？如果说都不能合法组织政党，那么宪法规定的结社自由又是什么？我们无法知道，如何叫合法组党？<br />
<br />
<br />
3，新民党不是一个有组织的政党。<br />
<br />
<br />
再次，孔强、王喜强的证言无法证明郭泉有组织行为，郭泉任命他们做省党部主席。按照新民党党章，这种不是组织行为，无非让他们散播理念，并无其他意图，其次，认同理念而没有上下控制系统不属于组织。试问，公诉人能提供一个正式的新民党成员名单吗？能提供新民党党主席和省主席之间的组织方式吗？如果说，郭泉认为的，认同他理念的就是新民党成员，这只是思想传播而已。组织是要双向的，没有成员和成员之间的互相认可，光一个人说你是我的党员，就不是组织。试问，孔强同意没有，王喜强同意没有？更何况郭泉与孔强、王喜强连面都见过。《现代汉语词典》第1820页解释组织的意思是（1）安排分散的人或者事物使其有一定的系统性或整体性[6]。郭泉的新民党并无有这样的形式和功能。相反，公诉人提供的至少三个以上证言明确证明，这些和郭泉有接触的的人都认为新民党不是一个实际存在的政党，只是一个网络党。<br />
<br />
<br />
辩护人认为，郭泉的所谓新民党只是一个理念的政党，谁认可了，无须任何手续即可成为党员，无须手续，即可以不成为党员，谁都可以成为党部代主席，这种党，根本没有什么组织纪律，入党手续，如果将其和中共共产党的党章和组织比较，这根本无法算一个现代政党。<br />
<br />
<br />
4，在中国国土上，组党未必犯罪。<br />
<br />
<br />
首先，我国法律并没有规定组党就是犯罪，相反宪法规定了公民的结社自由。其次，如果说，在中国的国土上成立除中国共产党和 8个民主党派之外的政党就是犯罪，那么请问，在台湾、香港的那么多政党，他们是不是在中国领土内，他们是不是政党？为什么连战、吴伯雄、陈菊等来，我们中国共产党领袖还要亲自迎接这些非法组织的领导？为什么不把他们绳之以法？显然，在中国的国土上还是有结社自由的，哪怕这个党只有一个人。既然能在国宾馆设宴迎接国民党的吴伯雄，那么就不能以组党为由在法庭上审判新民党的郭泉。<br />
<br />
<br />
第五，指控郭泉通过互联网策划民主革命蓝色运动，七日在家革命等活动。这些都是涉及宣传所谓民主理念，并无社会危害性，并不构成颠覆国家政权，推翻社会主义制度。<br />
<br />
<br />
1，民主蓝色革命运动只是一种理念，并不违法，不构成犯罪。<br />
<br />
<br />
请问郭泉这个民主革命蓝色运动、和七日在家革命，除了他的文章外，有何证据证明有任何响应，又有什么实际社会危害性，如果一个人写一篇文章都构成犯罪，我们和明、清时期的文字狱又有何区别？<br />
<br />
<br />
蓝色民主运动，所说的就是民主方式变更执政党。我们宪法也认同民主，从毛泽东到Hu Jintao都说民主是个好东西，为什么郭泉宣传民主就是犯罪。人民是否有权更换执政党？如果有，那么郭泉就没错。另外，这蓝色民主运动号召大家佩戴蓝色的领带，手帕等，这只是一个行为艺术，更名没有什么颠覆国家政权的意思，推广民主理念，何罪之有？<br />
<br />
<br />
2.&nbsp; &nbsp;七日在家革命只是一个理念，并无社会危害性。<br />
<br />
<br />
郭泉的所谓七日在家革命，首先没有社会危害性。事实上也没有任何社会危害性。请问，谁响应了？公诉人没有任何证据。<br />
<br />
<br />
其次，郭泉的文章中明确表明：七日在家理论的目的就是让中国共产党召开政治协商会议，提前终结一党执政，从而开始多党竞争，也不存在颠覆政权，推翻社会主义制度的目的。其手段是在家不和专制者合作，不为专制者服务，是为了避免街头流血，这种目的和颠覆国家政权、推翻社会主义制度没有关联性。<br />
<br />
<br />
再次，谁又会响应这个理念？按照三个代表理论，中国共产党都代表全体人民了，谁又会响应郭泉的号召而不上班呢？何况，大家也都是要养家糊口的。辩护人想问，如果郭泉忽然想出一个办法说，要引爆太阳，灭绝全人类，那么是不是也是颠覆国家政权罪呢？<br />
<br />
<br />
七日在家革命，理论上涉及的是罢工权，82宪法之前，都规定了罢工权，82宪法虽然没有规定，没有规定的理由是工人都当家作主了，不需要罢工了。这说明罢工权仍然保留在人民手中。只不过无需使用而已。社会在向前发展，如果75年文革中的宪法都有罢工权，说之后人民就失去罢工权了，那么我们国家宣传的进步又是什么？郭泉呼吁大家行使罢工权，如果人民觉得没有需要，就不会响应，如果人民觉得需要，也可以行使。决定权在人民，如何能颠覆国家政权？<br />
<br />
<br />
第六，起诉书指控郭泉借维权之名，策动推翻社会主义制度既没有事实依据，逻辑上也是错误的，维权维护的是合法权益，法律是维护国家长治久安的，维权不会推翻社会制度。<br />
<br />
<br />
&nbsp; &nbsp;&nbsp;&nbsp;首先，郭泉对维权人士说的，只是希望通过多党竞选，解决他们的合法权益被侵害的事实，如前所说，这种理论并不会导致颠覆国家政权，推翻社会主义制度。<br />
<br />
<br />
其次，没有任何客观证据证明郭泉在借维权之名策动推翻社会主义制度。郭泉文章中有时会有些言语涉及到共产党，但是，往往是针对中国共产党的独裁行为，如前面所说，既然，中国共产党没有搞独裁，对着空气打拳，有什么所谓损害？其次，政党不等于社会义制度。起诉书的逻辑也是错误的。<br />
<br />
<br />
第三，郭泉帮助军转干部、抗美援朝老兵、城市退伍志愿兵、被买断工龄者、城市被拆迁房屋者、城市被经租房屋者、乙肝病毒携带被歧视者等弱势群体维权，维护的都是他们的合法权益，帮助他们解决问题，只会是帮助社会稳定，而不会颠覆政权。<br />
<br />
<br />
第七，根据刑法第105条的规定，颠覆国家政权罪是指：组织、策划、实施者、及积极参加者和其他参加者，应该是组织犯罪（即有组织的多人共同犯罪），郭泉一个人分身乏术，单人根本就无法构成本罪。<br />
<br />
<br />
刑法规定组织、策划、实施颠覆国家政权、推翻社会主义制度的，对首要分子或者罪行重大的，处无期徒刑或者十年以上有期徒刑；对积极参加的，处三年以上十年以下有期徒刑；对其他参加的，处三年以下有期徒刑、拘役、管制或者剥夺政治权利。从条文看，郭泉到底是首要分子还是积极参加者，抑或是其他参加者。如果是首要分子，那么其他次要分子是谁？如果郭泉是积极参加者，那么谁是首要分子。因此，以此罪名来指控一个单人犯罪是错误的，逻辑上也无法成立。<br />
<br />
<br />
如果说新民党是一个众多党员的旨在颠覆国家政权、推翻社会主义制度的政党，那么为什么今天站在被告席上只有郭泉一个人，其他的党员为什么不被追究刑事责任？<br />
<br />
<br />
<br />
审判长、审判员：<br />
<br />
<br />
&nbsp; &nbsp; 任何人都必须为自己的行为负责。历史是后人写就的，法官的职能是依法审判，依照的法律是现行的宪法、法律。我国刑法105条，法律规定非常明确，颠覆国家政权、推翻社会主义制度的，才构成犯罪。郭泉的理论只反共产党的所谓“独裁专制”，提倡全民福利的民主社会主义制度，提倡由人民选择执政党，其行为不满足犯罪构成四要件，完全不构成犯罪。<br />
<br />
<br />
<br />
根据我国法律，合议庭法官要依据事实证据内心确认被告人郭泉构成犯罪后才能判刑，如果有悖于法，是迫于某种行政的指示或压力做出错误判决，法官对人民、对历史、对自己都是应当负有责任的。<br />
<br />
<br />
毋庸讳言，郭泉或许是一个走在时代前面的人，却并没有失去理智，从庭审看，相反条理清楚，曾经也是你我的同行，郭泉的主要理论无非是看得超前、想得超前而已，其行为没有社会危害性。我们在历史教科书中可以看到，任何走在时代前面的人都会受些误解，譬如孙中山作为先行者，当时被满清政府通缉，中国共产党的创建者李大钊、陈独秀被当时政府定罪。历史告诉我们那些将先行者定罪的人，都已经把自己钉在了历史的耻辱柱上。前车之覆，后车之鉴。<br />
<br />
<br />
76 年以前国民党指控陈独秀危害民国罪时，国民党的御用文人说：反对并图谋颠覆国民党者，即为反对并图谋颠覆国家，即为危害民国，亦即为叛国。”76年过去了，中国共产党领导下的人民民主专政的国家，不应该再重新用这个荒唐逻辑，中国共产党有本事赢得民心打败国民党，辩护人相信，应当能超越这个“反党即叛国”的荒谬结论！76年前的辩护词今日仍能用，辩护人不禁为之深深遗憾。<br />
<br />
<br />
综上，辩护人认为，郭泉是无罪的；如果，郭泉被判有罪，历史将一定会做出公正评价！像林昭、李九莲、钟海源、张志新、遇罗克那样。我们不希望历史的悲剧重演。<br />
<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp; <br />
辩护人： 斯伟江、郭莲辉 <br />
