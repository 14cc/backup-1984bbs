													<h2>一审辩护词？</h2>
						
						
																											<div id="postmessage_200278" class="t_msgfont">郭 泉 案 辩 护 词（一审 08/07/2009）<br />
尊敬的审判长、审判员：<br />
<br />
<br />
根据中国法律，我们接受被告人郭泉的委托，担任他的辩护人，为维护被告人郭泉的合法权益，人格尊严，维护宪法法律的尊严，特提出如下辩护意见，请合议庭慎重考虑。<br />
<br />
<br />
（特别申明：辩护人对郭泉案件事实只提出法律评价，不涉及政治上的评价）。<br />
<br />
<br />
基本观点：辩护人完全不同意起诉书指控的所谓郭泉构成颠覆国家政权罪。两辩护人经认真调查研究后，得出的一致结论是，从犯罪构成要件看，郭泉是完全无罪的，应立即无罪释放。<br />
<br />
<br />
76 年前，也在江苏，章士钊为陈独秀危害民国案辩护词中说，“政府不等于国家，民国的主权在民，复辟国体才是叛国，才是危害。否则，不论对于政府或政府中何人何党，有何抨击，都是正常的，只有半开化的国家才会以此“临之于刑”。半个多世纪过去，这个辩护词似也可以用在郭泉身上，只不过，76年后的辩护人想加上一句，“执政党不等于国家政权”，以言论批评、甚至抨击执政党、政府的行为不构成颠覆国家政权罪。<br />
本案没有任何证据能证明郭泉试图推翻国家政权和社会主义制度。如果指控一介书生试图以和平方式来推翻拥有几百万军队和警察，拥有核武器的世界强国的国家政权，显然是匪夷所思的。<br />
<br />
<br />
第一， 国家政权难以和平颠覆，郭泉涉及的颠覆国家政权罪罪名本身是值得商榷的。<br />
&nbsp; &nbsp;1，【国家】国家政权是指什么？国家是领土、人民、政府、对外主权的混合物[1]，如何颠覆？能被谁颠覆？正如一位印度的先哲对亚历山大大帝说，你看征服者来，去，何曾对这块土地带来什么？作为一个领土、人民、政府、对外主权的集合体，理论上是无法进行颠覆的。不管你如何定义颠覆这个词的意义。<br />
<br />
<br />
【政权】政权是什么？政权是统治一个国家的权力。这种统治权是由国家机构代为行使，国家政权机构包括国家机关组织本身及担任国家机关的政务官员和事务官员。这个统治权，在中国封建皇朝，这个权力属于皇帝，所以称之为家天下。所谓“普天之下，莫非皇土，率土之滨，莫非皇臣。”<br />
<br />
<br />
【主权在民】在现代国家，这个统治权力属于人民。无论是美国的we people(我们人民)，还是中国《宪法》的人民民主专政。国家政权机关是代表人民行使政治权力，他们本身只是受托人，而不是他们本身就产生、拥有的这个政治权力。我国78宪法把公民权利和义务一章放在国家机构之前，就说明了这一点。因此说要颠覆这个政治权力，无论是意图颠覆人民对国家的主权，还是颠覆国家机关，唯一的路径就是违背人民的意愿，使用暴力征服人民，从而变成家天下或者其他形式的独裁，将人民从主权者的地位变成被统治者。这就是章士钊所说的：“民国的主权在民，复辟国体才是叛国，才是危害”。用其他方式如欺骗的方式是难以征服人民的，正如我国一贯的说法，群众的眼睛是雪亮的。加上人民都有理智，能分辨清楚到底是自己拥有主权，还是皇帝拥有绝对权力。<br />
<br />
<br />
【民主选举】如果人民通过民主方式更换政权机关中的政务官，那么这种方式，可以称之为民主选举。如国家领导人更换。如果人民通过民主的方式要求改变政权机构的设置，那么这种方式，可以称之为政治改革。如修宪设立中央军事委员会。如果人民通过暴力的方式，推翻欺压自己的暴政，可以称之为革命。如辛亥革命。如果不是人民通过暴力方式革命，而是外族武力入侵，或者一小部分人通过暴力（政变、武力）掌握政权，这是征服（如满清）或军政（如北洋军阀）。这些统治者不代表人民，也不是民主，而辛亥革命是代表人民，故称之为革命。这是历史书上的常识。<br />
<br />
<br />
【颠覆】颠覆，按照字面意思就是将对象倒置，引申为推翻。推翻、倒置都是需要用物理力量的，用文字是无法完成推翻、倒置的。何况是拥有数百万军队的国家政权。<br />
<br />
<br />
【结论】因此，不仅理论上无法颠覆国家政权，而且行为上也不能颠覆。这个罪名显然是无法用和平的方式来触犯的。而郭泉一介书生，被指控颠覆国家政权罪，以一人对13亿人，以一介书生对抗数百万雄兵的核国家，显然是不可能的。公诉人可以说，本罪是行为犯，不论结果，但是，如果这个行为和结果之间没有因果关系，那么我们说不构成犯罪。郭泉写的新民党党纲中明确写明，不用武力，不建军队。这就更清晰地表明了――郭泉不构成颠覆国家政权罪。<br />
