郭泉老干部母亲大闹法庭 你们才在颠覆国家政权<br />
<br />
――郭泉老干部母亲大闹法庭：儿子 我为你骄傲<br />
<br />
李亦菲，慧宏　希望之声　2009年08月08日&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;字体大小：&nbsp; &nbsp; 　<br />
<br />
8月7日，原南京师范大学文学院副教授郭泉涉嫌‘颠覆国家政权罪'一案在宿迁法院开庭审理，郭泉母亲、著名作家顾潇出席旁听，郭母说共产党法庭给她上了一堂民主启蒙课，原不理解郭泉的郭母离开法庭前大喊：&quot;儿子，你是伟大的，我为你骄傲。&quot;<br />
<br />
郭泉的母亲对记者说，年近70，这是人生第一次出席庭审，还是自己儿子被审。见到后，很欣慰，郭泉穿着囚服，带着手铐，脸很白，人也瘦，像个书生，但郭泉和律师的辩护非常精彩。<br />
<br />
【录音】：&quot;我当时没有来得及伤心了，忘记了它的规定，怕儿子看不到，特意站起来了，我说，儿子啊，小泉，妈妈在这边。自行辩护，我发现我儿子沉着稳定，就像他是郭教授，在课堂上对学生讲课一样的。他辩驳的太精彩了，那些不值得一驳的理由，我儿子驳的非常完满。我觉得我的儿子的辩护词和那两个我请的律师的辩护词简直就像博士生和小学生对话一样，不是一个平台啊。&quot;<br />
<br />
郭母说自己是体制内的一名党员记者，经历过文化大革命，知道政治的残酷，不想儿子因此遭遇不测。作为母亲，她从来就不希望儿子多伟大，只求平平安安，但到庭听审后改变了她的想法。<br />
<br />
【录音】：&quot;我从来没有试图了解过他的思想内容究竟是什么，可是昨天以后，是共产党的法庭给我上了启蒙课了，从那个所谓的罪行，从我儿子，两位律师无罪辩护，我知道了，我儿子真的是没有罪，应该是对这个国家，对这个民族是有功之人啊。使我了解了这一切，我真的很佩服我儿子，而且，我发现，他的思想很坚定，不会变的。&quot;<br />
<br />
郭母告诉记者，她本无意出现庭审，是宿迁法院一位叫仲佳的多次给她打电话，让她来旁听，她拒绝一次又一次，对方也不罢休。<br />
<br />
【录音】：&quot;三番五次的打电话让我去，说你能确定不来吗？你是家属，我说我身体不好，不能去。我儿子通过律师跟我讲了，妈妈身体不好不要去，我也决定不去了。我说你怎么老打电话，倒让我矛盾了。因为我最近非常想我的儿子。&quot;<br />
<br />
郭母来到宿迁，法院没有像承诺的那样给她三张票，而是想让她一个人无票进去，她生气拒绝。她说，安检比上飞机还要严，手表都要留外面，尤其呼喊郭泉时，法警厉声呵斥并推搡，不断发生的事故导致她情绪失控。<br />
<br />
【录音】：&quot;我从那个审判长啊，便衣警察满布啊，军警林立啊，武警拉着戒严啊，如临大敌啊，你晓得。收我们的手表，为什么其他旁听的人都带手表呢？骗我，欺骗我们，把我当成罪犯一样的来对待，使我在那里面情绪失控，我就在那里大喊大叫，我大叫大喊大骂的结果，他们没敢打我。&quot;<br />
<br />
中午休庭时，郭母向审判长请求，希望面对面的看一眼郭泉，被拒绝。<br />
<br />
【录音】：我希望我跟儿子面对面地、眼睛对着眼睛地我就跟他讲两句话行，我说：&quot;十分钟行不行？&quot;&quot;不行！&quot;我说：&quot;五分钟行不行？&quot;&quot;不行！&quot;我说：&quot; 就两分钟，就讲一句话。&quot;他说：&quot;不行！&quot;不行还骂我两句，然后我就叫起来了，我就说你们是根本不是公开开庭，你们是秘密开庭，我说我儿子有什么错的？我说现在你们来逮捕我吧！你们才在颠覆国家政权呢，带你们手铐！骗子！骗我来的。然后我就高叫的喊，我说：&quot;儿子！妈妈以前不了解你！你太棒了！你厉害！妈妈理解你了。&quot;<br />
<br />
郭母表示原只想平和的在这里，没想要大吵大闹，没想到共产党伤害人是那么狠，不要说人权，连人性都没有，看到她不断的吃速效救心丸，也不理。<br />
<br />
【录音】：&quot;我倒不是为我的儿子带了手铐、穿了囚服伤心。我实在是原来不知道共产党是这么样的会说谎，这么样的对人这么坏，我真是为共产党太，把我一个文人竟然变成了一个疯子了，一个泼妇了。我也不愿意这样，我真的被激怒了，我现在才晓得，人到了极端愤怒的时候，是个什么感觉，我根本就没有打算能回南京，我就恨不得一 头跟他撞死，我就在那里叫喊，你们才是颠覆国家政权呢！你们来逮我吧！&quot;<br />
<br />
下午的时候，郭母身体开始浮肿，心情不好，实在看不下去，当天还要回南京，就想提前离开。由于始终没有正面看儿子一眼，就要和儿子告别。郭母心情极差，叙述中，她老人家已经是泣不成声。<br />
<br />
【录音】：&quot;5点钟的时候，事情看不下去啊，跟律师招招手，我站起来，儿子啊，妈妈回南京了，我说我以前不了解你，我骂你，我以后不骂你，儿子，你做的对，你太伟大了，我说，我为你骄傲，我说。几十个人来推我。汽车站买了最后一趟车回南京了。喉咙脓的哑的说不出话来了。&quot;<br />
<br />
说到最后，郭母的情绪已经非常激动，她说，【录音】：&quot;我太伤心了，你妈70岁了，你不怕死，你妈还怕什么死啊，这些话我儿子听不到了，他给人家关起来了。请你告诉所有关心郭泉的人，也关心我的老阿妈的人，谢谢他们了，啊！&quot;<br />
<br />
郭泉曾在07年底向中共领导人Hu Jintao、Wen Jiabao写公开信，要求实行宪政改革；08年发起成立中国新民党；又曾撰文批评四川灾区学校豆腐渣工程。2008年11月被当局以涉嫌‘颠覆国家政权罪'逮捕。</div>

							
							
							
							
