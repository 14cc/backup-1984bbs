													<h2>新华时评：为上海楼市组合拳叫好</h2>
						
						
																											<div id="postmessage_858184" class="t_msgfont">2010年10月08日 04:24新华网【大 中 小】 【打印】 共有评论4条<br />
&nbsp; &nbsp; <br />
<br />
&nbsp; &nbsp; 新华网上海10月7日电（记者慎海雄、徐寿松）作为楼市风向标之一的上海，7日出台了调控新政。上海楼市新政的鲜明特点在于不是头痛医头式的“散打”，而是出了一套组合拳：动用银根、地根、税收、行政调节诸种手段，从供应和需求入手，加大双向调节力度，目标是坚决遏制地价、房价上涨势头，最终既要改善居民居住条件，又要促进房地产市场平稳健康发展。<br />
<br />
&nbsp; &nbsp; 房地产业是个系统工程，庞大、复杂、敏感，房价过高、交投过热只是房地产问题的外部表征。要解决当下楼市潜在的非平稳健康“因子”，调控的意志必须坚决、自觉、彻底，而调控的手段也必须是系统的、有针对性的。惟其如此，才可能期望其结果是有效的、健康的。上海此番楼市调控新意恰在此：下决心、动真格、成体系。具体表现有三：一曰“限”、二曰“压”、三曰“保”。<br />
<br />
&nbsp; &nbsp; 所谓“限”，限贷、限购是也，以抑制不合理住房需求。信贷方面，三套房停贷，不合规定的非本地居民购买不贷。购房数量方面，实行一定时期内限购，每户家庭只能新购一套房。<br />
<br />
&nbsp; &nbsp; 所谓“压”，即抑制房价暴涨、抑制投机性需求。上海动用税收杠杆，一方面积极做好房产税改革试点的各项准备工作，一方面将新房价格与土地增值税预征率挂钩，房价涨幅大，开发商被征收的土地增值税税率将会水涨船高，以削弱开发商涨价的动力。<br />
<br />
&nbsp; &nbsp; 所谓“保”，就是在上述指标手段之外，加强治本力度，即着力推进住房保障工作，从源头上增加居住用地供应总量，从终端上力推以保障性住房为主的大型居住社区建设，今后每年新增保障性住房20万套――这大抵相当于目前上海商品住房年销量的一半以上。<br />
