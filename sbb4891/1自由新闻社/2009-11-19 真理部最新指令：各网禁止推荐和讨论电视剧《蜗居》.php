剧中不同的角色、不同的年龄段、不同的阶层，似乎都在自己或在身边生动的活着。<br />
<br />
【家】对中国人来说有着一种特殊的意义，因此也成为了众多人的‘刚性’要求。并为此而不断奋斗，甚至碰到头破血流或是锒铛入狱。等到过后自己仔细品味，自己追求的就是梦想中的家吗？也许它只是一栋空荡荡的房子。而在这栋大房子中入住的主人也许早已不是你在描绘蓝图时本应该住在房子里的那些最初你深爱的人们。‘刚性’是什么？幸福是什么？也许在你为房子奔波而遍体鳞伤的时候，觉得最幸福的人是为你构筑终极目标的那些―开发商。<br />
<br />
【爱情】所有人眼中最神圣的两个字。对在不同阶段的人来说却能读出不同的味道。结婚几年的海萍感受着幸福、担忧、绝望;恋爱中的海藻感受着甜蜜、幻想、艰辛；婚后多年的宋思明也在体会着看透、寂寞与无奈。神圣的爱情给大家的带来其实也是五味杂陈形形色色。<br />
<br />
【选择】一个人好像一生下来就要不停的选择。学校、单位、爱人、哪怕是生命终结后的那块栖灰之地。然而可叹的是人们在选择过后往往会感叹当时为什么没有选择‘那样’的方式？现在想想围城实在是太有道理了，城外的人想进去，城里的人想出来。。。如果海萍重新选择她可能会更加务实，如果小贝重新选择会毅然决然的在经济上帮助海藻，如果让宋思明重新选择的话正像他说的那样：“不要太多的钱，每天去菜场斤斤计较，为发论文、评职称而与人争得面红耳赤，也为女儿考不上好学校而心焦。”<br />
<br />
电视剧浓缩了很多生活中的点滴，当然艺术是对点滴极端的表现。但是，为了不接近这条极端之路，现在的我们把它当作前车之鉴未必不是一个明智之举吧？<br />
