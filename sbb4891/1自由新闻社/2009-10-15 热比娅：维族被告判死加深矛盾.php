不过，热比娅・卡德尔仍强调，维吾尔将继续以和平手段争取权益。此前她通过各种途径多次尝试和中国政府进行对话，但毫无收效。热比娅・卡德尔希望国际社会能够帮助维吾尔族人的获得自治。她请求西方国家，敦促中国政府和维吾尔族人展开对话，并希望能够由联合国成立一个独立调查委员会前往新疆，实地了解情况。热比娅・卡德尔还呼吁国际社会：&quot;必须开始采取国际行动，取消死刑。&quot;<br />
<br />
为了她的人民，热比娅・卡德尔付出了巨大的个人代价。她的五个孩子仍然还在中国，另外她还失去了她在中国的全部财产。热比娅・卡德尔表示：&quot;我的两个孩子现在在监狱中，还有三个孩子和其他的亲戚都被监视着。在抗议活动后，我的孩子们背负着巨大压力被迫在公开场合、在电视上辱骂我。如果不是被逼迫的话，他们是绝不会这么做的。&quot;<br />
