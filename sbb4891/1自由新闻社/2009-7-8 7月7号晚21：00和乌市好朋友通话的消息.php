<br />
昨晚21：00和一个在那座城市的好朋友通话，得知一点真相。打完后快7月8号的零点了，很累，也没想到写啥。<br />
她非常非常想上网，告诉大家真实的情况。可是上不了叫我写点啥，我拖到今天来补点吧。<br />
<br />
比如宣布今天全城断水；比如所有超市都关门儿；街道上更是没人，有的是疯跑的提刀的人，和警察。也有逃命的人。最繁华的地方也已冷寂。剩下的街上也躺着尸体。这是城市的情况。交通已管制。昨晚开始宵禁。<br />
<br />
昨天她告诉了一些基本事实：<br />
<br />
1，闹事的是少数极端份子，推测是1000左右；这1000左右人她说可能是来自南疆，完全不代表大部分维族人。她的亲友里不乏维族人，大家平时友好和睦，完全没人去理你维族我汉族就像你是武汉人我是重庆人一样，标签贴得并不深入。<br />
<br />
2，她说据当地电视台报道，开始是没有汉族人去砍人的，后来又100多冲动汉族人要为同胞报仇提刀上街的事，都是某个公司或者某个团体同事朋友遇害大家情绪激动联合起来，当地政府已经通过公司领导让这些人冷静了下来。<br />
<br />
3，有些十分残忍的事：比如把女孩子扒光了从楼上扔下去；把孩子的肉一点点割下来；一个母亲在逃命时把孩子藏在了楼下的藏身处，自己躲到了楼上。然后亲眼目睹暴徒把孩子的头开了花。<br />
<br />
4，有感人的事：比如在一条维族饭馆很多的街上，汉族人逃命经过时，维族老板极力的收留保护。一进餐馆就给他们发白头巾，又藏好人。当暴徒经过时，问有没有汉族人经过，维族老板们矢口否认。<br />
<br />
