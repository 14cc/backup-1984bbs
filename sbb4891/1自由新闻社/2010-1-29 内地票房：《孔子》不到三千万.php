7 国产片《矮子多情》80 80 3 广东省公司<br />
<br />
　　《孔子》微弱优势拿亚军<br />
<br />
　　去年传记电影《梅兰芳》口碑出众，从官方到民间都比较认可，陈凯歌打了个漂亮的翻身仗，而如果从票房数字来核算，未必能支撑其制作费和宣发费，真实情况只有内部人士知晓，而外界的猜测是顶多打平，但是，不少影片除市场回收之外还有很多潜在价值，这也是古装大片一再被开发的重要原因，相信也是《孔子》浮出水面的原因之一。但是，与《梅兰芳》处处受宠相比，《孔子》的遭遇显得非常尴尬，甚至落寞。<br />
<br />
　　1月22日（周五），《孔子》全面上映，首映周末三天票房只拿到2800万，影片放映场次将近14000场，吸引观众人次80万左右。拿《孔子》与贺岁档中其它同量级的影片进行比较，只能说《孔子》的市场表现中等偏下：《花木兰》遭遇《2012》拦截，首周末三天票房3050万，《刺陵》和《风云2》与《三枪拍案惊奇》同台，前者首周末四天半票房3400万，后者3000万票房，很明显，《花》＞《孔》＞《刺》＞《风》，而《孔子》是其中制作预算最高、阵容最豪华、声势也最浩大的一部。<br />
<br />
　　从五大院线的票房情况来看，《孔子》都能抢到亚军位置，但只是以微弱优势超过《邻家特工》，没能大幅拉开距离。在规模最为庞大的万达院线，《孔子》一共拿到525万，在周票房总产出中所占比例约为11%。中影也是《孔子》片方和发行方，旗下两条院线排片自然会有一定倾泻，在中影星美院线，《孔子》放映场次2955场，约占院线放映场次20%，票房产出465.6万，约占院线票房12.7%；在中影南方新干线，《孔子》拿到359万票房，所占市场份额约为11.5%。在新影联院线，《孔子》产出票房196万左右，约占院线周票房的10%。《孔子》在上海联合院线拿到274万票房，所占市场份额仅为8.6%左右，据院线方说法，这个表现已经超出了事前预期。<br />
<br />
　　片方在第一时间公布《孔子》三天票房3800万，这个数字迅速遭到网民的质疑，有些网友自发贴出自己观看影院的上座率及观众反应，反驳新闻通稿里“一票难求”的说法。而根据电脑终端售票数据公布的2800万票房，已经证实了片方虚报票房，这是继去年《阿童木》被点名批评之后最新一起虚报事件。<br />
<br />
　　《孔子》民间被抵制<br />
<br />
　　《阿凡达》的火爆程度远超想象，自然可以成为《孔子》战不利的最大理由，但它的说服力并不高，影响其票房表现的原因还有很多。<br />
<br />
　　首先是该不该拍《孔子》？某研究报告显示，在中国电影观众的年龄结构中，18-29岁约占60%，30岁以上观众约占32%，主力观众人群是年轻人。年轻人对“重述历史”或者“戏说”并无太大反感，只要影片好看就行，所以，改编自小说《三国演义》的《赤壁》、出自《聊斋志异》的《画皮》、甚至是乱扯的《见龙卸甲》，都拿到了不错的票房表现。但《孔子》呢？它会是年轻观众感兴趣的电影吗？应该不是，孔子只是作为教科书上的符号而存在，不是一个电影人物形象。然后是怎么拍《孔子》？胡玫是电视剧导演，掌控这么大的项目本身就有一定危险性，所以要找编剧陈汉摄影鲍德熹等等一线主创来夯实基础，请来周润发和周迅则是为了提升电影的品质以及市场的热情，但这些硬件并不是这部电影的核心，问题的关键是《孔子》如何与现代观众产生联结。孔子位高，他无法像周瑜孔明那样被创作成少年英雄，更不可能被戏说，所以，就连孔子会武功都需要导演站出来解释，更遑论子见南子的设计了。事实上，《孔子》从一开始就注定了是高空走钢丝的命运。<br />
<br />
　　观众没必要从《孔子》的市场成败中了解什么，他们关心电影好不好看，值不值票价。在某网站举行的三地网友观影及调查活动中，北京观众认为影片像一部电视剧，前半段剧情还算完整，后半段周游列国支离破碎，观众等不到高潮只能等电影结束，最后他们宽容地打了77分；深圳观众普遍认为影片剧情苍白，故事缺乏戏剧性，特效无新意而且很多余，他们打出72分；广州评审团的活动在大地影院举行，得出了与片方推崇的制作精致和演技出彩相同的结论，98分显得过分注水，弃之不用。大体上，75分是网友给出的比较客观的评价。<br />
<br />
　　之所以要提到客观，是因为大量网友和影迷被《孔子》逼迫成不客观，比如以影迷为主的时光网，总分10分《孔子》拿到3.7分，其中0-1分占62%，1-2分占13%，而在文艺青年聚集的豆瓣网，52%的网友打出1颗星，虽然这些评分已经不具备参考价值，但它肯定是网民表达愤怒的一种方式。<br />
<br />
　　之前周润发在首映式上说：谁要是看了《孔子》不掉眼泪，谁就不是人。在经历了《黄石的孩子》和《七龙珠》等片惨败、因价格谈不拢辞演《赤壁》被评无情义之后，周润发还为《孔子》如此卖命，也算是敬业，但这句话着实伤害了观众的情感，很快遭到各路讨伐。后来片方放出将投放2500个拷贝，创拷贝最高纪录的消息，很快有人查证得到胶片拷贝500个左右的结果。接下来最严重的事件就是《阿凡达》被下片，18日网上流出院线和影院将在1月22日撤下《阿凡达》胶片版拷贝的消息，媒体从影院方获得证实，而导演胡玫表示不知情，然后又说“如果《阿凡达》真是因为《孔子》下线，我是说如果，作为一个中国导演我感到非常温暖，为国家保护国产电影的措施感到温暖，保护国产电影，我们应该有这样的态度。” 院线影院对政策性下片有微词，而观众更是愤怒，他们拒绝被看《孔子》。事实上，在一线城市的多厅影院，《阿凡达》胶片版的上座率确实下降不少，就算《孔子》不搞这套政策性下线，影院也不会可以不给《孔子》安排场次，但是，在没有数字厅或厅很少的老影院，如果撤下《阿凡达》就只能看《孔子》，还有广大的二线城市影院，他们本来看到《阿凡达》就晚，如果胶片版被撤可能就看不到。自己掏钱买票，却要被指定，中国电影第一次掀起广泛民愤，竟然是因为《孔子》。很快就有最新通知，有3D设备的影院停放《阿凡达》胶片，无3D设备的影院可继续放映《阿凡达》胶片（包括数字版）。至此，观众维护自身权益取得局部胜利。<br />
<br />
　　在这场闹剧中，《孔子》从头到尾都是输家，它不仅输掉了票房，更输掉了观众的信任，影片在还没放映之前就已经丧失了过亿的可能性。观众绝不是不爱国产片，但是，国产片也要给观众爱它的理由。<br />
<br />
　　广东特色经营<br />
<br />
　　广东省电影公司近年来一直比较活跃，因为方言的关系，主要发行一些低成本粤语片和国产片文艺片，这两类影片发行投入低，风险也比较小，虽然票房规模不大，但影响力不小，比如《十分爱》、《盲山》和《公园》，票房和影响都比较突出。目前市场上映影片其实不多，《阿凡达》之所以票房高涨，是因为它把原本不看电影的观众拉进了影院，但对于而对于那些已经看过影片的观众来说，他们需要新的选择，浪漫喜剧《矮仔多情》此时出现，是符合市场需求的，影片仅在广东地区上映，首周末三天票房80万左右。<br />
<br />
　　广东奥飞动漫从玩具制造业起家，其产业链模式正在向日本动漫产业靠拢，“铠甲勇士”特摄系列也很有东瀛风格，号称中国版奥特曼。奥飞最早推出特摄电视剧《铠甲勇士之光影传说》，然后推出系列玩具，而《铠甲勇士之帝皇侠》则是剧场版的尝试，据称大量使用电脑特效。这部特摄片主要瞄准已经放寒假的中小学生，影片在广东、上海等地排片情况还算不错，首周末全国票房130万左右。如果有玩具等相关产业的支持，特摄片做成系列也不是没有可能。<br />
<br />
　　《孔子》哑火，《铠甲勇士之帝皇侠》和《矮仔多情》发行规模偏小，只能起到点缀作用，市场核心仍然是《阿凡达》，部分影院撤下胶片版有一定影响，但影响不会太大，影片本周再入1亿7000万元，环比下滑27%左右。截止本周，《阿凡达》在内地电影市场累计放映21天，累计票房已超过6亿9000万元，约合1亿300万美元，票房产出仅排在法国之后。在北美，《阿凡达》本周拿到3490万美元，第七周放映仅下滑18%，其跌幅控制能力与当年《泰坦尼克号》16%的表现相差无几，而北美总票房距离超越6亿美元仅几步之遥，预计在两周之后突破。在海外市场，《阿凡达》本周再入1亿美元，累计票房已达12亿9000万美元，它在周六突破《泰坦尼克号》海外票房12亿4000万美元的纪录，而突破《泰坦尼克号》18亿4300万美元全球票房纪录必然将在下周实现。<br />
<br />
　　《阿凡达》胶片版部分下线，也为《邻家特工》释放了不少空间，影片第二周上映逆跌幅收得2300万，环比上涨39%左右，影片累计票房已有4000万。《名侦探柯南》也因为扩大放映规模和寒假的到来扩大收入，影片本周进账320万，环比上涨236%，影片累计票房420万左右，在日本动画剧场版中排名靠后。<br />
<br />
　　本周票房总产出2亿3000万左右，环比下滑不到12%，其中三部新片所占市场份额约为13%，《阿凡达》仍占到74%。<br />
<br />
　　下周展望<br />
<br />
　　去年《喜羊羊与灰太狼之牛气冲天》创造奇迹，被视为中国动画产业的领军势力，今年《喜羊羊与灰太狼之虎虎生威》如期而至，能否再次黑马冲出，下周即见分晓。<br />
