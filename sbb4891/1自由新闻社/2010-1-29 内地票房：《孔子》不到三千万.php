																											<div id="postmessage_457707" class="t_msgfont">内地票房：《孔子》不到三千万<br />
<br />
来源：搜狐娱乐 作者：文/五明 2010年01月29日18:47<br />
<br />
　　上周依旧是《阿凡达》的天下，单片票房产出占周总票房90%以上，成龙好莱坞新片《邻家特工》开场黯然，仅拿到1650万，相当于一般进口买断片的表现，《名侦探柯南》干脆避开风头，选择只在少数城市上映，票房产出非常有限，而《十月围城》和《三枪拍案惊奇》则是进一步收割后期票房。《阿凡达》吸金能力之强劲超乎想象，也打乱了不少影片的阵脚，其中就包括本周全面上映的《孔子》，留给业界及观众的猜想是在如此巨大的压力下，它这第一步能踩上第几级台阶。<br />
<br />
