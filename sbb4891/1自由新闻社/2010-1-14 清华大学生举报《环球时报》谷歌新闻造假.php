然后我就再没有理他了。文中出现的计算机系也完全是他胡乱推断的。实际上我从来没这么说过而且我也不是计算机系的。至于他的报道中出现的“我非常担心Gmail因此会用不了”，这是我跟翡翠台的记者说的话。就在我不再理他的时候，一抬头发现翡翠台的摄像机已经出现在我面前了，也就回答了他们几个问题，他们的职业道德就好很多，问的都是客观问题，也没有有意问为难我的问题。<br />
