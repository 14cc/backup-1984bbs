&nbsp; &nbsp;&nbsp; &nbsp; 今天忽然看到环球时报上面出现了一条新闻（<a href="http://tech.huanqiu.com/net/web/2010-01/688463.html" target="_blank">http://tech.huanqiu.com/net/web/2010-01/688463.html</a>）<br />
<br />
清华大学一名计算机专业的杨姓大学生向《环球时报》记者表示，他是听说这一新闻之后专程骑自行车来到谷歌大楼附近打听消息的。他说：“我很担心谷歌退出中国市场会对像我这样的用户带来很大影响，我非常担心Gmail因此会用不了。但如果谷歌公司想要逃避审查，我无法接受。”<br />
<br />
环球时报这种恶意强加观点和虚构与路人的对话的行为严重伤害了我的名誉，他根本就没有对我进行采访，我也没有接受他的采访。如果是一个普通人，这已经是一种不道德行为了，更何况是一名记者，环球时报有这样的记者，它的新闻也不会有什么可信度了。任何一个了解我的同学都知道我是不可能说出这样的话的。“打听消息”这几个字恐怕也只适合他自己吧。<br />
<br />
实际上，我在当天下午2点50的时候骑车去了google，我是听说google的logo上面有人献花，所以想去看看的。而且作为一个曾经想去google实习，并曾经收到google实习生招聘邀请（附文末）的学生，我来google也有着祭奠的想法。<br />
<br />
到了那里，我没有看见花，于是就随便找了一个人问，那个人正好就是这位环球时报的记者。以下是我们的对话：（回忆有细节不清楚，但这就是我们对话的原貌）<br />
