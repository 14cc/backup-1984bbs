													<h2>官方出面坐实了这条消息</h2>
						
						
																											<div id="postmessage_806000" class="t_msgfont"><strong>湖南掩盖金浩等茶油致癌物超标 称为社会稳定</strong><br />
2010-08-31 10:05:29　来源: 人民网(北京)<br />
<br />
核心提示：21日，金浩茶油公司澄清被查出致癌物超标6倍的传言，湖南质监局迅速发布抽检合格结果公告。该局内部多位人士证实今年3月金浩已被查出超标。金浩的内部文件也显示部分超标产品已秘密下架。一位质监局高层表示粮油问题关系国计民生，不公开问题是为了维护社会稳定。<br />
<br />
人民网8月31日报道&nbsp; &nbsp;8月21日，在湖南金浩茶油股份有限公司（下简称金浩公司）一天前公告“澄清被查出致癌物超标6倍的传言”之后，湖南省质监局迅速发布抽检合格结果公告。一场关于“国家免检产品”金浩茶油“致癌”的“谣言风波”看似逐渐平息。<br />
<br />
然而，本刊记者调查发现，无论是湖南省质监局的抽检公告，还是金浩公司的“澄清公告”，均未提及一个关乎公众食品安全的重要事实。<br />
<br />
根据国家《食用植物油卫生标准》，食用油苯并芘含量的最高上限为10微克/千克，而国家质检总局此前关于湖南省部分茶油的抽检结果显示，苯并芘含量已达到60微克/千克，为最高上限的6倍。<br />
<br />
湖南省质监局官方网站却始终未公布上述抽检“合格”结果，只是通报给了省内的官方媒体。而本刊记者根据公告中的电话询问质监局多个部门，均获得了“不知道公告情况，也不知道产品到底是否合格”的答复。<br />
<br />
私下里，湖南省质监局多位内部人士向本刊记者证实，早在今年3月，金浩公司等即被查出致癌物“苯并芘”严重超标。之后企业按要求进行整改，对相关产品进行召回。金浩公司在其内部汇报材料中也称，相关超标部分原产地湖南、采用“浸出”工艺生产的茶油已下架。但这一情况至今未对公众公开，也没有公布被下架或召回的品牌及其产品型号。<br />
<br />
记者近日在北京、广州、长沙等几家大型连锁超市查询时，超市销售人员均称，从未得知金浩茶油有质量问题，也从未收到召回金浩茶油的通知。在这些超市，金浩茶油依然热卖。<br />
<br />
金浩公司目前年产值11亿元，在湖南各地有多处生产基地。董事长刘翔浩是全国人大代表，他曾称公司将在2012年以前实现上市目标。<br />
<br />
慢性致癌物苯并芘<br />
<br />
苯并芘又称苯并（а）芘，英文缩写BaP，是强致癌物之一，食用受其污染的食品，虽然不一定表现出明显的急性毒性，却具有公认的致畸、致癌的慢性毒性。<br />
<br />
许多国家已经规定了相应的膳食摄入量和食品的限量标准。按照中国国家GB2716-2005《食用植物油卫生标准》要求，在食用植物油类产品中苯并芘的安全限量为不超过10微克/千克。<br />
<br />
金浩公司在内部自查汇报材料中披露，其通过对现有的浸出毛茶油、浸出精炼一级油、压榨毛茶油、压榨精炼一级油等分别进行抽样检测，已得知利用现代浸出技术产出的毛茶油苯并芘含量在60微克/千克左右不等。<br />
<br />
压榨法和浸出法是山茶油提取的两种方法。压榨法是用物理压榨方式，从油茶籽中榨取山茶油，是一种传统的提取工艺；浸出法则是用物理化学原理，用食用级溶剂从油茶籽中抽提出山茶油的一种方法，是目前国际上公认的最先进的提取工艺。<br />
<br />
根据金浩公司等企业在市场所销售的多种茶油特点，企业比较普遍采用上述两种方法互补的做法，即将油茶籽经过压榨获得“压榨原茶油”后，“油饼”内残存茶油，再用浸出法充分地抽提出来，获得“浸出原茶油”。<br />
<br />
对于苯并芘含量过高发生的主要原因和环节，目前尚无官方的权威说法。而根据金浩公司的自查报告得出的结论，该公司问题主要出在茶油提取的浸出环节，传统榨出工艺则不存在危险性。<br />
<br />
金浩公司自查报告还显示，公司主要从农户收购茶饼。<br />
<br />
为了提高出油率，农户可能对茶籽反复烘烤、蒸炒，如温度控制不好，容易导致烧焦现象，生成苯并芘，残留在茶籽的外壳上；而压榨后苯并芘就全部残留在茶饼中，在萃取茶饼中的残油时，残留的苯并芘也被大量萃取出来，通过高温又与溶剂油发生化学反应，最终导致浸出后毛茶油苯并芘含量不断提高。<br />
<br />
在这个过程中，用烧焦的茶饼进行再次粉碎，用120度的高温蒸炒后再进行压榨，得出的压榨浸出油中，苯并芘的含量检测值就已经超标，达到26微克/千克，后再经过六号溶剂油对茶饼萃取完成后，所获茶油的苯并芘含量迅速上升为60微克/千克。<br />
<br />
湖南一位农业专家表示，茶油提取在湖南已是多年技艺，反复高温加热烘烤蒸炒茶饼容易产生苯并芘是常识，企业一般也都会通过进一步精炼工艺和添加活性炭等方式来降低苯并芘的含量。但显然，并非每一个企业的生产过程都能如此严格。<br />
<br />
掩盖与谎言<br />
<br />
国家质检总局和湖南省质监局早在今年3月即获悉部分茶油企业抽查结果不合格的情况，企业也在内部自查中发现致癌物“苯并芘”严重超标。<br />
<br />
据本刊记者了解，7月2日，湖南省质检局在长沙市东塘新东方大酒店，组织获证生产加工企业技术负责人、检验人员，各市州局食品科科长及检测人员、相关专家等召开紧急会议，研究讨论解决植物油产品苯并芘超标问题。金浩公司代表也在会上汇报了公司自查和难题攻克的研究结果。<br />
<br />
当时会上，有人提出要严格防止事件经由媒体公开。<br />
<br />
湖南省质监局一位高层在接受采访时表示，粮油问题关系国计民生，不公开问题是为了“维护社会稳定”。<br />
<br />
8月中旬，网络上传出金浩茶油等部分食用植物油生产加工企业的若干批次产品苯并芘含量超标的消息。8月20日，金浩公司在对消费者的声明中称，“被查出致癌物超标6倍”是谣传，称“我公司郑重承诺，旗下主打产品‘金浩茶油’系列高档食用植物油质量安全可靠”，并且在主动与监管部门沟通，适时发布权威信息。<br />
<br />
第二天，8月21日，作为监管部门的湖南省质监局在当地媒体公告了“茶籽油生产加工企业专项监督抽查结果”，所附的23家企业抽检结果均显示合格，其中包括金浩公司。<br />
<br />
然而，本刊记者注意到，金浩公司抽检的产品型号为“低温冷榨油茶籽油”，并非市场上常见的、经过高温环节压榨浸出的茶籽油或者茶籽调和油。送检产品的生产时间为4月7日。<br />
<br />
对于为何不对此前已发现问题的型号产品进行抽检，或者扩大抽检型号范围，湖南省质监局没有做出解释。该局一位工作人员回应说，“抽检只对企业送检的来样负责，不可能对每一个批次都检测。”<br />
<br />
而前述湖南省质监局高层认为，金浩公司的问题情有可原：“它不像三聚氰胺是人为添加，而是因为制作工艺处于提高的过程”，而在问题发现之后，金浩等茶油公司及时改进了生产工艺，其中包括提高脱臭温度、更换脱色用剂和降低冻化温度等。<br />
<br />
8月17日，湖南省质监局食品安全处一位负责人回忆，事情发生后省里高度重视，第一时间由省质监局派出一位教授级专家协助企业查找超标原因。从3月到6月，专家和金浩公司产品质量控制部联合进行了技术攻关。“改进之后的生产批次我们进行了抽检，均没有再发现问题。”<br />
<br />
湖南省质监局的一份文件中显示，国家质检总局在江苏检出相关茶油致癌物超标之后，湖南省质监局即要求各州市质监部门就地封存所有茶油，并启动召回程序。<br />
<br />
然而，对于此前已发现问题型号产品的处理，湖南省质监局并没有给出明确意见。根据本刊记者在广州、北京和湖南多地的各大超市走访获得的反馈信息，金浩公司既未对外公布有问题的茶油批次信息，也未曾向超市下达召回或者下柜产品的信息。<br />
<br />
8月25日，本刊记者在长沙多个超市仍看到照常销售的金浩茶油或者其他品牌茶油，产品生产日期也多在3月或者3月之前。<br />
<br />
由于企业和政府的掩盖，消费者无法从正常渠道获得有关产品调查和整改的信息。而多位消费者看到网络上的传言之后都表达了担忧。<br />
<br />
来自广州南沙区的刘辉从数年前即食用该品牌，他表示，消费者无从了解食用油是否安全，这全依赖于国家相关部门的监管。既然曾经检查出问题，就应该实施公开召回，“像药品企业如果发现问题，每一剂药都要从使用者那里回收，食用油的安全重要性一点都不比药品低”。<br />
<br />
根据《食品安全法》规定，“食品生产者发现其生产的食品不符合食品安全标准，应当立即停止生产，召回已经上市销售的食品，通知相关生产经营者和消费者。”<br />
<br />
国家质监总局制订的《食品召回管理规定》还要求：“自确认食品属于应当召回的不安全食品之日起，通知消费者停止消费。”<br />
<br />
显然，湖南省的此次所谓“召回”既不合法，也不合规。<br />
<br />
扩张诱惑<br />
<br />
公开资料显示，油茶属山茶科山茶属植物，是中国特有的木本食用油料树种，有2000多年的栽培和利用历史，与油橄榄、油棕、椰子并称为世界四大木本油料植物，主要分布在长江流域及其以南的14个省（区、市）。<br />
<br />
湖南省林业厅党组书记、厅长邓三龙撰文指出，中国食用植物油供应严重不足，每年缺口达到700万吨，都需要通过大量进口来解决。开发木本食用油已成为全球解决食用油的主要渠道和趋势。<br />
<br />
作为健康型高级食用植物油，茶油市场近年来发展迅猛，整个行业都处于急剧扩张状态。<br />
<br />
2009年11月，国务院批准颁布了《全国油茶产业发展规划（2009－2020年）》，此后，湖南、江西、浙江、安徽、广西等主要油茶产出大省，从地方政府到当地企业都非常积极参与竞争。<br />
<br />
而湖南无论油茶林面积还是茶油年产量，均居全国第一位，全省茶油加工企业达到2000多家。<br />
<br />
早在2008年，湖南省人民政府即正式出台《关于加快油茶产业发展的意见》。<br />
<br />
湖南油茶重点县市区已纷纷成立了由党委一把手任顾问、政府一把手任组长的油茶产业发展领导小组，以支持当地茶油产业发展。<br />
<br />
“金浩茶油”正是来自于有着“中国油茶之乡”的祁阳县，也是当地一大支柱企业，亦被地方政府作为重点扶持企业。此次苯含量超标的湖南茶油企业中，也以金浩公司规模最大。<br />
<br />
但在迅速扩张的茶油市场和企业雨后春笋般地冒出背后，无论是安全监管还是技术实力却都还欠火候。<br />
<br />
<br />
中国政法大学教授吴景明认为，在这个事情中地方政府处理很及时，但依然存在隐瞒重大食品安全信息的现象。地方政府有政策倾斜无可厚非，但不能以消费者健康为代价，在监管过程中要及时提出风险警示，“目前这种处理方式的危害性在于，这样一来，企业很可能认为万事有政府撑腰，在产品生产上从此有恃无恐”。<br />
<br />
中国社会科学院食品药品产业发展与监管研究中心执行主任张永建认为，不公开透明的处理方式加大了消费者的安全风险，同时也可能导致“劣币驱逐良币”的出现，进而可能危害整个茶油产业的发展。<br />
<br />
“在毒奶粉事件中，也曾经出现过地方政府和企业秘密召回产品的现象，但最终这个盖子没有捂住。”北京大成律师事务所律师陈北元说，现代食品加工手段复杂多样，生产者和消费者存在着严重的信息不对称，因此才会有一系列法律法规把消费者作为弱势者进行保护。<br />
<br />
(本文来源：人民网 )</div>

							
							
							
							
