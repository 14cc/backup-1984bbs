													<h2>金浩承认茶油致癌物超标 近9吨问题茶油未召回</h2>
						
						
																											<div id="postmessage_808358" class="t_msgfont">本报讯（记者廖爱玲杜丁）近期，国内知名山茶油品牌“金浩茶油”被曝致癌物苯并(a)芘超标。昨日，金浩公司在其官方网站挂出《致广大消费者致歉信》，承认曾有9批次纯茶油产品存在苯并(a)芘超标，并表示已在今年3月和4月进行过两次全面排查和召回。不过，记者昨日仍在北京市场发现生产日期为今年3月份的金浩茶油销售。<br />
<br />
湖南有关部门曾责令召回<br />
<br />
8月中旬以来，网上传出湖南金浩茶油被查出含致癌物超标6倍，随后，金浩公司发声明辟谣。但据媒体报道，湖南省质监局内部人士透露，产品问题早在年初就已发现，该局还责令企业整改并对相关产品召回，但始终没有向公众发布。对此，当地质检官员称是为维持稳定。<br />
<br />
金浩承认召回未通知到位<br />
<br />
事件急转，昨日，金浩公司在其网站发表致歉信，承认其公司生产于2009年12月3日至2010年3月17日的9批次纯茶油存在苯并（a）芘超标问题。此前曾召回两次。<br />
<br />
据介绍，9批次产品总生产量42.458吨，其中湖南省质监局稽查总队在公司仓库当场封存22.361吨，公司召回11.152吨。<br />
<br />
不过记者注意到，对剩下的8.945吨茶油，金浩公司并没有具体说明究竟流向哪里，只表示如因召回疏漏，消费者还有以上9批次产品，可及时联系公司进行退货或换货，但并未提及赔偿问题。<br />
<br />
针对“秘密召回”，金浩承认存在“未及时公开不合格产品批次、召回过程未通知到位的问题”，向消费者深表歉意，将按规定继续通知。<br />
<br />
退换货仅限于不合格产品<br />
<br />
昨晚，记者联系到湖南金浩公司位于北京的营销总公司，一位负责接听解答问题的汪女士表示，目前市面上销售的金浩茶油大多是4月1日之后的产品，质量是有保证的。而4月1日之前的产品只发现9批次有问题，因此退换货也仅限于这些公布的批次。<br />
<br />
北京超市暂不下架金浩产品<br />
<br />
而昨晚，记者在位于广安门外一地下超市看到该牌子的油，货物架上共摆放了三瓶，其中一瓶为金浩橄榄油，另外两瓶为金浩茶油，两瓶金浩茶油的生产日期显示为今年的3月份。据该货柜的工作人员介绍，这个牌子的销量不好，两三个月没售出一瓶，“前两天该厂家自己买走了两瓶，再没见人买。”这位工作人员表示，商场进该牌子的货物也不多，也就货柜上的三瓶。<br />
<br />
此外，北京家乐福、物美、华联等超市负责人表示，“金浩”茶油产品仍在正常销售，但超市方面会密切关注事态发展，在没有得到权威部门的正式通知，或厂家的召回通知之前，卖场暂不会将金浩产品下架。<br />
