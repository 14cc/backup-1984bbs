													<h2>问题茶油追踪：金浩茶油称近期将对公众有交待</h2>
						
						
																											<div id="postmessage_805163" class="t_msgfont"><a href="http://business.caing.com/2010-08-31/100175412.html" target="_blank">http://business.caing.com/2010-08-31/100175412.html</a><br />
<br />
2010年08月31日 07:54　 本文来源于财新网<br />
<br />
<i>金浩茶油一位负责人承认今年在质监部门抽检中被查出问题，并进行了两次召回</i><br />
<br />
　　【财新网】（记者 赵何娟）针对8月30日财新传媒《新世纪》周刊报道的“问题茶油秘密‘召回’”问题，湖南金浩茶油股份有限公司（下简称金浩茶油）一位奉（音）姓负责人当日称，该公司已经两次召回相关产品，其中问题虽然没有对外公告，但此前已报告质监主管部门，不存在瞒报的情况。该负责人并未透露召回产品的品种和批次等信息，只是称近期会给公众和媒体一个交待。<br />
　　然而，财新记者近日在北京、广州、长沙等几家大型连锁超市查询时超市销售人员均称，从未得知金浩茶油有质量问题，也从未收到召回金浩茶油的通知。<br />
<br />
　　上述负责人对财新记者表示，没有对外公开，是因为最初抽检超标的产品是在厂里，只有一两个批次，且还未销售到市场上去，一直在研究怎么公开的问题。他同时又承认，3月份被查出问题之后，公司就对已销售到市场上的相关产品有问题的或怀疑有问题的都进行了召回，并且是两次召回。<br />
<br />
　　这位负责人认为，公司已经很负责任地处理问题，也很配合质量监督局的调查和处理。“细节尚不方便透露太多，也还没有最终结论。”对于记者询问所谓“最终结论”是指什么问题的最终结论时，该负责人未给予解释，仅称近期会以新闻发布会或者别的形式公布，给公众和媒体一个交待。<br />
<br />
　　金浩公司目前年产值11亿元，在湖南各地有多处生产基地。董事长刘翔浩是全国人大代表，他曾称公司将在2012年以前实现上市目标。<br />
<br />
　　金浩茶油的一份自查汇报材料显示，国家质检总局今年3月对湖南省食用植物油产品进行风险监测时，检出该省部分食用油中苯并芘含量超标。随后，湖南省质监局对该省植物油产品进行专项抽查时，也反映出不少食用植物油中苯并芘超标，“有的甚至超标很严重”。<br />
<br />
　　根据国家《食用植物油卫生标准》，食用油中致癌物“苯(a)芘”的含量最高上限为10微克/千克。而金浩公司对其现有的压榨毛茶油、压榨精炼一级油、浸出毛茶油、浸出精炼一级油等分别进行抽样检测，压榨毛茶油和压榨精炼一级油的苯并(a)芘含量在5微克/千克以内，说明压榨工艺不会使苯并(a)芘超标，但利用现代浸出技术产出的毛茶油苯并(a)芘含量在60微克/千克左右不等。<br />
<br />
　　金浩公司的汇报材料解释了毛茶油苯并(a)芘含量高的原因：公司从农户中收购茶饼，而农户为了提高出油率，可能对茶籽反复烘烤、蒸炒，在高温下容易生成苯并(a)芘。此外，浸出工艺中，溶剂油在55度温度作用下，萃取粉碎蒸发过的茶饼里的残油，这个过程会导致苯并(a)芘含量进一步增高。<br />
<br />
　　汇报材料还称，精炼可以降低毛茶油中的苯并(a)芘含量，但即使经过精炼的精炼成品油，含量仍然在20微克/千克左右。换句话说，该公司的浸出精炼茶油中的苯并(a)芘仍然超标。<br />
<br />
　　最终，金浩公司在汇报材料中称，通过改正精炼工艺方法，已将浸出精炼一级油中的苯并(a)芘含量降到5微克/千克以内，即不再超标。<br />
<br />
　　8月20日，金浩公司在其网站刊登的声明中称，近期互联网上部分网络论坛及微博出现了关于“金浩茶油被查出致癌物超标6倍”的谣传，公司承诺旗下主打产品金浩茶油系列高档食用植物油质量安全可靠，本次网络不实谣传“不排除是竞争对手恶意炒作”。<br />
<br />
　　分析人士指出，如果金浩公司关于毛茶油苯并(a)芘含量高的解释成立，那么，其他茶油企业乃至生产其他一些种类植物油的企业，部分产品也可能存在苯并(a)芘超标的问题。实际上，除了金浩公司，其他茶油公司也大量从农户中收购茶饼，也同样在采用浸出工艺。而金浩公司等企业直到国家质检总局和湖南质监局今年抽检发现问题之后才开始自查并改正工艺，令人匪夷所思。<br />
<br />
　　湖南省质监局人士一位接受《21世纪经济报道》采访时则称：“事实上，只要是通过浸出法从压榨后的‘油饼’中提炼油品，不管是茶油，还是花生油或橄榄油等植物油类，都可能或多或少导致苯并(a)芘超标。”<br />
<br />
　　似乎是为了呼应金浩茶油的声明，打消公众对所谓“不实谣传”的担心，湖南省质监局8月下旬通过《湖南日报》等当地官方媒体公告“茶籽油生产加工企业专项监督抽查结果”，所附的23家企业33种茶油产品抽检结果均为合格，其中包括金浩公司的部分茶油产品。<br />
<br />
　　在金浩茶油位于湖南长沙的办公室里，金浩茶油总裁办人士一再要求记者向湖南质监局了解详情，因为质监局已对产品安全情况发出公告，可到湖南质监局网站官方网站查询。但经记者查阅，湖南质监局官方网站并没有前述茶油抽查结果。而且，湖南质监局至今没有公开此前对该省植物油产品专项抽查时“不少食用植物油中苯并(a)芘超标”的情况。<br />
<br />
　　7月2日，湖南省质监局在长沙市东塘新东方大酒店，组织获证生产加工企业技术负责人、检验人员，各市州局食品科科长及检测人员、相关专家等召开紧急会议，研究讨论解决植物油产品苯并(a)芘超标问题。金浩公司代表在会上汇报了公司自查结果和解决办法。<br />
<br />
　　当时会上有人提出要严格防止事件经由媒体公开。湖南省质监局一位高层则在后来接受采访时表示，粮油问题关系国计民生，不公开是为了“维护社会稳定”。<br />
<br />
　　根据金浩公司网站，毛茶油一般是经过简单过滤处理的茶籽油，又称“土茶油”，其特点是有独特的茶籽油清香，很多中老年朋友较喜欢这种味道，但相当一部分年轻消费者则不喜欢。但土茶油含有较多杂质，这些物质的存在容易引起茶油变质，而且对人体健康会造成一定影响，国家关于油脂的规定中已经不允许毛茶油进入市场销售。<br />
<br />
　　上述分析人士说，如果金浩公司汇报材料属实，其浸出精炼成品油苯并(a)芘含量在20微克/千克左右，其超标程度没有浸出毛茶油那么严重，消费者无需恐慌，但金浩茶油秘密“召回”事件的关键在于，政府和企业面对问题产品选择秘密处理的方式，且召回不彻底，侵害了公众利益。<br />
<br />
　　财新传媒《新世纪》周刊主编王烁在当期杂志“编辑絮语”中感慨，在信息不透明的情况下，消费者会选择安全，相信各种可靠不可靠的传说，乃至阴谋论。如果不告知金浩公司哪个年度哪个批次的茶油致癌物超标，消费者会拒绝所有金浩公司的茶油。如果不告知哪家公司的茶油致癌物超标，消费者会拒绝所有茶油。更严重的是，如果披露不充分，消费者会拒绝相信以后的所有披露。■</div>

							
							
							
							
