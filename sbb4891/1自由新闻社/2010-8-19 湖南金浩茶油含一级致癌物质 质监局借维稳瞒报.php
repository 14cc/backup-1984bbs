二、如因召回疏漏，消费者还有以上9个批次产品的，请及时与我公司联系，我们将按照国家有关法律规定进行退货或换货。<br />
<br />
三、本次苯并（a）芘超标，非主观人为因素导致，属于生产工艺技术上的问题。经专家与公司技术攻关、反复论证得出，其产生的原因是：茶籽在烘烤、翻炒过程中，因温度过高、受热不均导致烧焦产生苯并（a）芘，在浸出环节出现工艺缺陷进而导致超标，以致公司产品出现了短时间内的几个批次的问题。但我公司采用浸出工艺生产的纯茶油仅占公司产量的1.08％。我公司已通过工艺改进，彻底解决了此问题，产品在4月份以来质量技术监督部门的抽查中均合格。<br />
<br />
四、在本次召回事件中，我公司存在未及时公开不合格产品批次、召回过程未通知到位的问题，由此对经营者和消费者带来的困扰，我公司深表歉意，并将按照《中华人民共和国食品安全法》及其实施条例的规定，继续通知经营者和消费者。<br />
