■ 名词解释<br />
<br />
苯并（a）芘：高活性致癌物<br />
<br />
苯并（a）芘又称苯并芘，是一种常见高活性间接致癌物，它不具直接致癌性，经细胞微粒体中的混合功能氧化酶激活才具有致癌性。<br />
<br />
在水体、土壤和作物中苯并（a）芘都容易残留。苯并（a）芘存在于煤焦油、各类炭黑和煤、石油等燃烧产生的烟气、香烟烟雾、汽车尾气中，及焦化、炼油、沥青、塑料等工业污水中。<br />
