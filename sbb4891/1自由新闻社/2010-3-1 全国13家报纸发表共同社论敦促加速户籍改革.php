																											<div id="postmessage_498176" class="t_msgfont">媒体不只是见证者：为什么我们要表发共同社论 <br />
<br />
大 中 小订阅 共有条评论15:18 2010-03-01&nbsp; &nbsp;&nbsp;&nbsp;张宏 <br />
<br />
经济观察网 评论员 张宏 <br />
<br />
今天，由《经济观察报》倡导，全国11个省、自治区和直辖市的13份报纸发表了名为《提请两会代表委员敦促加速户籍改革》的共同社论，共同表达出国内媒体关注并期望加速推进户籍制度改革的强烈声音。这是一件国内媒体业中罕有的大事，表现出国内媒体参与并推进中国改革进程的强烈愿望。<br />
<br />
此共同社论的出炉，是受到英国《卫报》一个相似项目的启发。去年9月，到访北京的《卫报》副总编辑Ian Katz和我在北京东四的一家酒吧见面。在这次会谈中，他邀请《经济观察报》参与12月哥本哈根会议时发表一篇关于应对气候变化的全球共同社论。“我们相信，如果来自全球各国的媒体能够在应对气候变化问题上达成一致，那政治家们更有理由、也更有迫切性就此问题达成一致。”Ian当时说。他希望媒体的努力可以推进全球应对气候变化的进程，因为事关全球环境问题，没有人可以成为局外人。后来，在12月7日哥本哈根会议开幕的第一天，全球56家媒体共同发布了那篇影响颇广的共同社论。<br />
<br />
毫无疑问，媒体所能做的事情就是“鼓与呼”，手握权柄的大人物才是最后的拍板决策者。尽管哥本哈根峰会的最终结果与各界事先预期有不小的差距，但媒体在此场合下不愿缺场、主动参与，至少是对政治家们形成了一定的压力，让尽可能多的人们也部分地参与到这场与他们看似无直接利益关系的全球性谈判中来。<br />
<br />
在历史发展过程中，媒体更多的是以见证者的身份出现，其被寄予的期望是客观公正地记录历史。但媒体的角色从不仅限于见证者，他们更是历史的推动者与开创者。<br />
<br />
中国改革开放的发端，肇始于1978年那场《实践是检验真理的唯一标准》的大讨论。这篇评论由《光明日报》率先刊登，隔日《人民日报》和《解放军报》同时转载，拉开了中国现代改革的序幕；1991年，在邓小平先生发表上海讲话后，《解放日报》发表皇甫平关于解放思想、推进改革开放的系列评论，一举打破了当时沉闷的舆论氛围，推动了新一轮的改革浪潮；在中国的当代史上，媒体从来就不是旁观者，而作为党的喉舌，媒体更有责任积极参与到推进中国改革的进程中来。<br />
<br />
建国60年，改革开放30年，历史的进程又走到了一个关系中华民族伟大复兴能否实现的关键时点。在今年两会召开前夕，我们听到了许多呼吁推进政治体制改革、调整经济结构的声音。在诸多改革建议中，我们选取了户籍制度改革作为今年《经济观察报》及另外12家报纸向两会代表委员倡言的主要议题，因为这是一项惠及华夏万千家庭的重大改革，也是时机已然成熟、民众期望最为迫切的改革之一。<br />
<br />
而国务院总理温家宝在昨天与网友的交流中也明确表示，“对新生代农民工要给予高度的重视，最重要的问题就是推进户籍制度改革，让那些长期在城市生活和工作，并具备一定条件的农民工融入城市。”<br />
<br />
我们相信，小人物才是历史进程中的英雄，他们才是推动历史向前发展的中坚力量。中国的户籍制度影响着我们每一个人，我们每一个人也都有理由和义务去关注、推动并完善这项政策。作为媒体，我们将这样的倡言以共同社论的形式向两会代表委员建言；而作为个人，你们对此共同社论的评论（不论是赞成还是反对）、转贴也都将推动整个中国社会变得更加民主、开放、“以人为本”。<br />
<br />
我们都不只是见证者，我们是参与者！<br />
