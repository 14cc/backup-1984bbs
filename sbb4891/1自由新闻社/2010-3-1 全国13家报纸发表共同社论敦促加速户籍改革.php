													<h2>13家报纸联合社论促户籍改革 统战烟幕还是小资抗争？</h2>
						
						
																											<div id="postmessage_498179" class="t_msgfont">打开电脑，赫然看见新浪的一个新闻标题为“全国13家报纸发表日本人论敦促加速户籍改革”。<br />
<br />
点开读了全文，我脑子里的第一反应是<br />
<br />
剑外忽传收蓟北，初闻涕泪满衣裳。<br />
却看妻子愁何在，漫卷诗书喜欲狂。<br />
<br />
十三家商业报纸，除了一个较为精英的新自由主义喉舌《经济观察报》，其他全部是都市报。上一篇博文里刚聊了两句都市报作为经济利益驱动下的小资产阶级急先锋，在新闻报道方面的异地监督职能。正在感叹都市报尚难以在全国性的话题上有所作为的时候，你突然发现，游击队联合起来了！德赛图说，处于弱势的读者就像游击队一样，使用他们的战术（tactics）来对抗被强势一方赋予了既定意义（strategy）的文本。如果我们单纯一些，我宁愿相信从“异地爆料”到“联合社论”都是都市报在为小资产阶级利益抗争的战术举动！<br />
<br />
虽然，我并不情愿把这些具有“职业道德”和“人文精神”的举动，如布尔迪厄所认为的，完全看作阶级斗争的结果。但你仍然能清晰地从社论的措辞里看到，都市报并没有在潜意识中把农民工的权益作为社论的出发点，而是很大程度上利用这个话题来张扬“自由”，“人生而公平”等非常小资产阶级的价值观。社论第一段里，“我们崇信人生而自由，人生而拥有自由迁徙之权利！”被作为宣言一样放在开头。第二段，强调了宪法的神圣和现有户籍制度的违宪本质。小资产阶级向来喜欢拿宪法说事，因为那个国家宪法都会捡好听的说，那是一个世俗宗教，用来表达事情“应该是怎样的”，而非事情“事实是怎样的”。<br />
<br />
最有趣的是第三段和第四段里的主语变化。第三段里，社论提出了进城务工的户籍性农民和他们子女的身份问题，“可是，他们的下一代仍然没有办法解决身份认同，他们的子女仍然背负着上一代的困惑，他们生活的城市仍然无法接纳他们，这才有了80后、90后农民工的称谓。我们要问，这样的隔离究竟还要持续几代人？”如果农民工是“他们”，那“我们”是谁呢？都市报的作者和读者，社论要谏言的人大代表是谁呢？潜意识里，农民工仍然是一个“他者”（the other）。我们所使用的语言下意识地复制了户籍造成的城乡二元体制。我们是文化人，他们是农民工；我们生在城市里，他们从乡下迁徙来……而第四段，作者又使用了一名农民工的口吻说“在同一座城市中，尽管我们与其他人一样为这座城市的建设奋斗多年，我们与其他人一样纳税，但没有户口让我们无法与其他人一样享受平等的就业机会，享受同等的医疗、教育、养老等等社会保障。因此，夫妻被迫两地分居，年老的父母无法与子女团聚，孩子无法获得良好教育。我们要问，这样的隔离究竟还要持续几代人？” 这是小资作者在下意识里的身份斗争，也是每一个有良知的“城里人”必须面对的问题：我是谁，他是谁。<br />
<br />
下面的行文先是指出户籍改革的积弊和政府行政中的腐败问题，然后又援引了温***指示和户籍改革方面的进步，颇有点打个巴掌揉两下的技法。我很高兴社论里提到了一些结构性的问题。我一直认为，腐败本身不是问题，是症状。是什么东西造成的腐败，才是我们要改，媒体要揭的东西。虽然短短一小段，我是觉得读来挺痛快的。<br />
<br />
第八段里，把户籍制度联系到“通过自由市场实现资源配置”的“高度”，我是感到有点失望。农民工不是资源，他们是人。对他们的权利的保障不是市场行为，反而应该是行政行为。我反对过度或者完全的自由市场，那样只能让小鱼喂大鱼，虾米全绝迹。政府的作用正是用来制衡过于贪婪的市场行为（不是辅助，也不是阻碍，是制衡）。这一段的观点充分暴露了都市报的小资嘴脸：把农民工问题看做为自由市场制度辩护的修辞，而不是实现“人生而平等”的理想。<br />
<br />
无论如何吧，事情不能完美。一步走完了，自然还有下一步的问题。解决问题和出现问题都不是原因或者结果，而是个过程。总的感觉挺高兴，同志仍需努力吧！</div>

							
							
							
							
