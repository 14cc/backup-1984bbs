													<h2>全国13家报纸发表共同社论敦促加速户籍改革</h2>
						
						
																											<div id="postmessage_497086" class="t_msgfont"><a href="http://news.sina.com.cn/c/2010-03-01/071119760645.shtml" target="_blank">http://news.sina.com.cn/c/2010-03-01/071119760645.shtml</a><br />
<img src="http://i45.tinypic.com/oo3mo.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
中国患户籍制度之苦久矣！我们崇信人生而自由，人生而拥有自由迁徙之权利！然此诞生于计划经济时代、不合时宜地存在数十年之久之弊政至今仍时时困扰着我广大民众，已到非革新不足以平息民怨，非革新不足以与时俱进之境地。为此，值全国两会召开之际，我们，全国11个省、自治区和直辖市的13家报纸发表共同社论，提请两会代表与委员们，善用你们手中的权力，敦促有关部委提出户籍改革的明确时间表，逐步以人口信息登记制度取代现行僵化的户籍制度，直至最终将其彻底消除！<br />
<br />
　　中国《宪法》明文规定，中华人民共和国公民在法律面前一律平等，国家尊重和保障人权，公民的人身自由不受侵犯。迁徙自由是人权和人身自由不可分割的组成部分，这是宪法赋予国民的基本权利。然而，现行的户籍政策却事实上造成了城市居民与农民、城市居民之间地位的不平等，制约了中国公民的自由迁徙，明显与《宪法》相违背。我们都知道，一切法律、行政法规和地方性法规都不得同宪法相抵触，这是加速目前户籍制度改革的法理基础。<br />
<br />
　　户籍制度分割了城市和乡村。“农民工”是对那些户籍在农村而身在城市打工的人群的特定称谓，最早的一代农民工，为城市的发展付出了自己的劳动，可是，他们的下一代仍然没有办法解决身份认同，他们的子女仍然背负着上一代的困惑，他们生活的城市仍然无法接纳他们，这才有了80后、90后农民工的称谓。我们要问，这样的隔离究竟还要持续几代人？<br />
<br />
　　即便在城市中，户籍制度也分割了城市的居民。在同一座城市中，尽管我们与其他人一样为这座城市的建设奋斗多年，我们与其他人一样纳税，但没有户口让我们无法与其他人一样享受平等的就业机会，享受同等的医疗、教育、养老等等社会保障。因此，夫妻被迫两地分居，年老的父母无法与子女团聚，孩子无法获得良好教育。我们要问，这样的隔离究竟还要持续几代人？<br />
<br />
　　户籍制度还是滋生腐败的温床。正因其稀缺，在很多城市户口成了被买卖的对象。有权者可以以此寻租，地产商可以以此为销售的工具，而千万的弱势群体要么付出金钱的代价，要么望洋兴叹地面对种种不公的待遇。我们要问，这样的不平等究竟还要持续几代人？<br />
<br />
　　温家宝总理不久前明确表示，中央已决定稳妥地推进户籍制度改革。而包括上海、深圳、广州等全国数十个城市都已经出台户籍改革的措施。在这些城市，居住证正在逐步取代暂住证，持证者将可享受与当地居民相同的社保、医疗、教育等公共服务。在一些城市中，农民工也正陆续被城市所接纳，他们迎来了迟到的尊严。同时，国家正在加快建立全国统一的社会保障社会化服务体系，实现社会保障关系跨地区转移接续。建立个人终身社会保障号，并尽快实现全国联网，这为现行户籍制度改革的加速奠定了基础。<br />
<br />
　　这些变化固然可喜，但在更多的地方，我们仍然失望地看到户籍这一无形而又沉重的枷锁，困住无数疲于奔命的人们。我们深知户籍政策之盘根错节，改革细节之错综复杂，然而我们更无法漠视那些已经、正在以及仍将因此政策而受挫、受苦的人们。对于他们，等待改革的迫切让每一分钟的等待都显得非常漫长。<br />
<br />
　　市场经济是由市场配置资源的自由经济，人的流动是市场经济题中应有之义。我们在欣喜地看到中国经济飞速成长的同时，我们也要警醒经济结构的转型已经迫在眉睫。人口红利正在消失，自然资源也非源源不绝，中国经济下一轮成长的动力已经更多地指向内部结构的调整与资源使用效率的优化，而非粗放式的外延扩张。户籍制度的改革不仅利于民生，更能加速中国的城镇化进程，为中国经济注入更多的活力。更重要的是，户籍制度的改革将能帮助确立中国以人为本的价值理念，成为中国社会各阶层均衡进步、构建和谐社会的基石。<br />
<br />
　　为此，我们呼吁全国两会代表、委员，运用人民赋予你们手中的权力，敦促有关部委尽快废除1958年颁布的《户口登记条例》，提出全国户籍制度改革的明确时间表，逐步以人口信息登记制度取代现行僵化的户籍制度，直至最终将其彻底消除。<br />
<br />
　　我们希望，我亿万国民，地无分南北，人不分城乡，都拥有同样的就业、医疗、养老、受教育、自由迁徙的权利。我们希望，一项为患数十年的弊政，能终止于我们这一代人，让下一代人真正享有自由、民主、平等的宪法赋予的神圣权利！<br />
