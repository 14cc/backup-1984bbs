<img src="http://lh3.ggpht.com/_kN_wnDAd2Uo/SNqBdyRFGFI/AAAAAAAAHcw/mSQ4JPjgZzo/install_step_4.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
简单的点Next就可以安装<br />
<br />
好了安装完成了，不是中文界面也不影响我们使用Tor.然后Tor就开始运行了。<br />
<br />
点任务栏右下角的洋葱头图标，就可以打开Vidalia 控制面板，点一下Srtar Tor就可以进入Tor网络了，欢迎进入地下网络。<br />
<img src="http://lh4.ggpht.com/_kN_wnDAd2Uo/SNqBg4Gm7SI/AAAAAAAAHdQ/BMiQ9yalS84/tor_run_1.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
正在寻找邻近节点并加入到TOR网络<br />
<br />
好了，下图显示，已经加入TOR网络了，点“Use a New Identity”就可以切换身份。使用TOR后，在未切换前，访问<a href="http://who.is/" target="_blank">http://who.is/</a> 这个网站显示的IP是不会变的，如果你想换一个IP,就切换一下身份，再访问 <a href="http://who.is/" target="_blank">http://who.is/</a> 就显示另一个IP了。当然，这时我们还不能使用Tor，因为还没有给浏览器配置好代理服务器。<br />
<img src="http://lh4.ggpht.com/_kN_wnDAd2Uo/SNqBj8SqCYI/AAAAAAAAHdw/UWYQdA48Bf4/tor_run_2.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
第二节 开始安装Foxyproxy<br />
<br />
先在Google里搜索Foxyproxy，然后可以找到这个下载链接：<a href="https://addons.mozilla.org/en-US/firefox/addon/2464" target="_blank">https://addons.mozilla.org/en-US/firefox/addon/2464</a> ，这界面是英文的，不过可以访问这个链接，是中文的 <a href="https://addons.mozilla.org/zh-CN/firefox/addon/2464" target="_blank">https://addons.mozilla.org/zh-CN/firefox/addon/2464</a> 于是我们能看下面的界面，点击安装到Foxyproxy。如果用google搜索到Foxyproxy 2.7，安装到Firefox 3时是不能安装成功的。<br />
<img src="http://lh5.ggpht.com/_kN_wnDAd2Uo/SNqeEfo9UKI/AAAAAAAAHiE/YlfbUw5j99U/Fullscreen%20capture%202008925%2021533.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
