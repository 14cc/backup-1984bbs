<img src="http://lh4.ggpht.com/_kN_wnDAd2Uo/SNqW_UhUimI/AAAAAAAAHg4/kopbwrKx1zY/9.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
双击Google邮件的模板，出现下面的“添加/编辑模板”，我把模板名字改为zuola.com，可以改成任何文字，倒是URL模板那里要填写准确，我们不懂正是表达式就用通配符，*zuola.com*这个模板就是表示，只要地址栏的网址（URL）里包含了zuola.com这词组就将通过这个代理服务器加载。我们这样设置了以后，如果打开zuola.com里一个包括了google picasa图片的网页时，只有zuola.com的文字流量是通过TOR代理服务器来下载，而picasa图片就不必经过Tor来下载，会直接下载而不经过代理服务器。凭这一点，就可以证明Foxyproxy+tor比 gladder强多了。<br />
