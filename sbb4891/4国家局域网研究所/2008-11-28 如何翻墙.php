<img src="http://lh6.ggpht.com/_kN_wnDAd2Uo/SNqWelkR-kI/AAAAAAAAHfg/zPBc26zVThY/aaa.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
