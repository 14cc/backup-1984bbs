来源：周曙光的个人新闻台&nbsp; &nbsp; <a href="https://www.zuola.com/weblog/?p=1221" target="_blank">https://www.zuola.com/weblog/?p=1221</a>&nbsp; &nbsp;&nbsp; &nbsp; [ 需要翻墙]<br />
<br />
TOR是一个分布式的p2p的代理服务器，Tor 的官方网站的语言是英文，位于 <a href="https://www.torproject.org/" target="_blank">https://www.torproject.org/</a>，但是TOR的官方网站的IP被GFW屏蔽了，所以我用rsync -az --delete rsync://rsync.torproject.org/tor /home/zola/tor.zuo.la 这个命令在Dreamhost的主机上做了一个镜像，方便国内的TOR用户访问并下载。下载地址是 <a href="http://tor.zuo.la/" target="_blank">http://tor.zuo.la/</a> ，我顺便教更多人使用最好的Tor代理服务器来突破封锁。<br />
第一节 如何安装TOR<br />
<br />
先去下载，最新版是0.2.0.31，但这个版没有中文界面，但不要紧，安装后几乎不用管它。如果你坚持要熟悉tor的所有选项并且不太熟悉英文界面，你可以到<a href="http://tor.zuo.la/dist/vidalia-bundles/" target="_blank">http://tor.zuo.la/dist/vidalia-bundles/</a> 下载0.1.2.9版的，如这个<a href="http://tor.zuo.la/dist/vidalia-bundles/vidalia-bundle-0.1.2.9-rc-0.0.11.exe" target="_blank">http://tor.zuo.la/dist/vidalia-b ... 1.2.9-rc-0.0.11.exe</a> 就是多国语言版。<br />
<img src="http://lh3.ggpht.com/_kN_wnDAd2Uo/SNqBbNh01PI/AAAAAAAAHcA/rvDD4QVXrNs/download_tor.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
下载，速度是289KB/s，Dreamhost的速度还可以，下载完成后右键占击后选择打开，<br />
<img src="http://lh3.ggpht.com/_kN_wnDAd2Uo/SNqBbaTTs8I/AAAAAAAAHcI/6qWfUwJG_Lc/open_tor.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
这时会有一个提示，如果你担心我的服务器上的软件被人入侵后篡改，可以下载 sig来对比一下PGP签名，用于校验文件是否在官方发布后被篡改。查看关于如何验证软件包签名的 FAQ，你可以据此确认你下载了我们希望你得到的文件。<br />
<br />
然后开始安装Tor了<br />
<img src="http://lh3.ggpht.com/_kN_wnDAd2Uo/SNqBcESQsOI/AAAAAAAAHcY/RhI8f1QUEYo/install_step_1.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
