自从我在博客上说了一嘴陈琳死前的秘密让人头皮发麻，这事儿到现在还没消停。当时写那篇博客，考虑欠周，不该那么说，只是把当时整个采访过程做得比较郁闷的心情抒发一下，没想到我认为没什么大不了的一句话，勾起了无数人的想象，从第一个问我“到底是什么不能说”一直到昨天最近一个问我“到底是什么不能说”都让我很烦。我说不说是我的权利，不是我的义务。甚至，还有人用知情权来吓唬我，嘿嘿，哥们是学法律的，你碰到我的硬处了。<br />
<br />
现在风波平息了，反正谁再问我也不会说的，但我可以解释一下我为什么不能说。因为那段内容不是在采访中得知的。我的录音笔有个毛病，就是关掉的时候后面大约有十分钟的信息会丢失，以前没注意过，后来在一次采访中，对方在采访马上结束的时候，突然妙语连珠，让我很高兴。结果回家一听，没有录上，让我大失所望，我觉得很奇怪，明明当时录上了，怎么丢了？于是我在家做了几次试验，发现还真有这个问题。谁懂这方面的技术，给解释一下，我用的是三星牌YEPP的破录音笔。<br />
<br />
这里顺便问问，有没有生产录音笔的厂家，赞助一下连质量好的录音笔都买不起的《三联生活周刊》，以免老出岔子。因为知道录音笔有这个问题，每次采访，到最后都要等上几分钟，然后再关闭。<br />
<br />
那次采访结束后，摄影记者开始给他们拍照片，我就把录音笔放在桌子上，一直没有关，然后让开了。我到一旁跟朋友聊天，小蔡大约拍了一刻钟的时间，这期间，被采访者一直在说话，当然，也被录下来了。那段令人“头皮发麻”的内容就是在这段时间录进去的。<br />
<br />
当晚，实习生在整理录音的时候问我，这段录音的内容要不要？我说，这是人家私底下的聊天，最好不用。但实习生还是很认真地把录音整理出来了。<br />
<br />
因为那段我不能用的录音不是发生在采访过程中，即使它再轰动、再刺激、再他妈什么，我也不能用，这是我该遵守的一个什么东西，不然以后谁还信任你。在这之后，我的朋友，我的朋友的朋友，我的朋友的朋友的朋友，所有八竿子打得着和打不着的人，以及有知情权诉求的人，还有那些喜欢哥德巴赫猜想的人，都好奇那些内容究竟是什么。短信、电话、邮件接二连三地过来。没想到人们的好奇心这么强。如果这件事发生在你身上然后让我知道了，你觉得我是该公开呢还是该公开呢还是该公开呢？<br />
<br />
我跟那天的被采访者都是第一次见面，没什么交情，也许这次采访完之后这辈子都不会见面，我完全可以不负责任地把这些东西写在杂志上，因为连朋友都不是，谈不上出卖朋友。但我认为，那是对人的不尊重。尊重这事有时候很小，但很重要。<br />
<br />
我喜欢在博客上编造朋友的隐私和段子，你看我每次编一些土摩托的段子，他一边乐的直揉搓自己的乳房，一边在博客上说：“王三表在博客上说的那些你们都别信，没一句真的。”我再补充一句：“但是真得很符合土摩托的。”<br />
<br />
那段录音，我删了，那段录音整理的文字，我毁了。没事别瞎鸡巴问了。我去看巴萨对国米去了。<br />
