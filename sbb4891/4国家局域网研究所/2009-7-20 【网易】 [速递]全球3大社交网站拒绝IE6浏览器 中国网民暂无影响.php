　<img src="http://img1.bbs.163.com/new/20090720/wsyz/ja/jason5ng32/ead5950ff67674afaa7ce48490f2d9de.gif" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
　　图：国内最多人使用的，但即将被抛弃IE6浏览器<br />
<br />
　　在浏览某个网页时浏览器突然崩溃，让你心神不宁了？你应该换一个浏览器了。浏览器是上网必备工具，你现在看到网事的这篇报道，因为你正在使用浏览器。<br />
<br />
　　近日，全球3个不同的社交网站表示将不再支持有9年历史的Internet Explorer6（简称IE6）浏览器，因为IE6严重阻碍了技术的进步，以致于开发人员不得不花大量的时间来让网页兼容IE6。目前IE6是国内使用率最高的浏览器，但由于一些原因，国内网民暂时不受到这场浏览器革命的影响。<br />
<br />
　　三大社交网站不再支持IE6<br />
<br />
　　北京时间7月15日全球最大的视频网站YouTube在网站左上角醒目地打出公告，YouTube将于不久后停止支持IE6浏览器。<br />
<br />
　　<br />
<img src="http://img2.bbs.163.com/new/20090720/wsyz/ja/jason5ng32/4d34c09ccce30a9fea51a847004f1028.png" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
　　图：Youtube表示将不再支持IE6<br />
<br />
　　差不多在同一时间，全球最大社交网站Facebook也给用IE6浏览该网站的用户提示升级到IE8、Firefox、Safari或Flock浏览器。<br />
<br />
　　<br />
<img src="http://img3.bbs.163.com/new/20090720/wsyz/ja/jason5ng32/eba1fcd8e6834190dc740205b61d3dda.png" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
　　图：Facebook对IE6用户提示升级到其它浏览器<br />
<br />
　　全球最大的微博客服务Twitter也在网站的醒目位置提醒IE6用户升级到更高的版本。<br />
<br />
　　<br />
<img src="http://img1.bbs.163.com/new/20090720/wsyz/ja/jason5ng32/35eb28145ffd507cb771130c040f5726.png" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
　　图：Twitter建议IE6用户升级到更高的版本<br />
<br />
　　IE6阻碍技术进步<br />
<br />
　　IE6在2001年时的确够用，因为当时的网页是相对比较简单的。但对于目前丰富多彩的网页来说，它是远远不够的。因为很多用户不愿意或没察觉到需要升级到更新的浏览器，网页开发者需要花大量的时间来开发兼容于IE6的网页，这种兼容是妥协而且带有损失的。<br />
<br />
　　第五代的网页技术HTML5支持视频、音频、二维绘画等现在的网页技术不具有的功能。然而，IE6并不支持HTML5，开发人员要么放弃对IE6的支持，要么放弃使用HTML5，也就是说，IE6在互联网标准的推行的过程中扮演者绊脚石的角色，阻碍技术的进步。<br />
<br />
　　多个更好的浏览器可以选择<br />
<br />
　　IE系列的浏览器普遍浏览网页是比较慢的，而且很容易就“卡死”。其它公司推出了众多更优秀的浏览器，这些浏览器能让我们更快更稳定地访问网页，而且功能比IE系列的浏览器更强大。<br />
<br />
　　这些浏览器包括最快的浏览器谷歌浏览器、Safari，功能最强大的浏览器Firefox。<br />
<br />
　　中国网民受影响甚小<br />
<br />
　　由于Facebook、Twitter、Youtube目前在国内遇到访问故障，国内网民几乎无法打开这些网站，所有中国网民几乎不受到这场浏览器革命的影响。<br />
<br />
　　而同时，由于国内没有任何一个大型网站站出来表示拒绝IE6，国内除了浦发银行和招商银行，其它所有网上银行都必须依赖于IE核心的浏览器。有理由相信IE6这款“老掉牙”的浏览器在国内还会存活好几年。这在一定程度上阻碍国内IT技术的进步。<br />
