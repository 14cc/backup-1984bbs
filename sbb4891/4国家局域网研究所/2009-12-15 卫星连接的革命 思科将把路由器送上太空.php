													<h2>卫星连接的革命 思科将把路由器送上太空</h2>
						
						
																											<div id="postmessage_374420" class="t_msgfont">作为互联网路由器领军企业，思科将进军一个新的领域，届时他们将把一个路由器安装在卫星上送入地球静止轨道。这将是思科在美国国防部推出的名为“太空互联网路由”(IRIS)项目中取得的首个重大进展。<br />
<br />
思科7月在旧金山召开的用户会议上介绍了30个他们正在发展的新业务。与此同时，思科在会上表示，他们计划在今年年底推出其中一个新业务。<br />
<br />
作为互联网路由器领军企业，思科将进军一个新的领域，届时他们将把一个路由器安装在卫星上送入地球静止轨道。这将是思科在美国国防部推出的名为“太空互联网路由”(IRIS)项目中取得的首个重大进展。IRIS项目最终将会让电缆和光纤均无法到达的地区能够以低廉的花费和更加方便的方式接入高速互联网。<br />
<br />
思科IRIS项目总经理Greg Pelton表示，卫星将承载互联网数据，并通过地面基站与互联网相联，尽管如此，它们实际上是一个独立的网络。地面基站先用特定频率向卫星发送数据，然后卫星再将把这些数据转发给其它预先确定的地面基站。服务提供商和政府部门等用户需要租用特定频率。<br />
<br />
Pelton称，卫星连接属于互联网上不连续的点对点连接，这是因为在太空中没有路由器的缘故。如果通信卫星有了路由器，那么通过不同的地面基站或卫星，它们将可以接收IP数据包并将这些数据包发往多个地方，从而根据需要随时建立新的连接。与挑选和租用一个特定连接相比，用户只需要支付使用卫星作为其物理构架当中的一部分的互联网服务费用。<br />
<br />
卫星顾问Mark Chartrand指出，太空路由还可以降低延时。Chartrand解释称，由于目前路由只能在地面进行，因此数据包每次由一个卫星传输到另一个卫星上时，都需要先发送至地面基站，然后再由地面基站转发，每一次转发都产生四分之一秒的延时。<br />
<br />
Chartrand表示IRIS项目无疑将解决这一环节所产生的延时问题。他称：“这一方案将让卫星变得更加智能，而且可以避免转发。”<br />
<br />
目前诸如铱星等卫星虽然能够彼此间进行通信，但是它们没有使用通用标准的IP。实际上，目前卫星技术很大程度上由昂贵的专利设备组成。<br />
<br />
Pelton称，两个新近研发成果已经为IP路由器进入卫星领域铺平了道路，其中一个研发成果可大幅提升卫星的交换容量，将其传输速率由每秒2Gb提升至150Gbps。这一提升得益于使卫星接入ka波段的技术，以及被称为“点波束”的新型天线技术。目前部分卫星已拥有多个天线，每一个天线均可以聚焦一个特定的区域，这比使用一个天线覆盖整个大陆相比先进许多。Pelton介绍称，这些“点波束”天线可以同时使用相同的频率，这相当于提升了同一频率上的数据传输量。<br />
<br />
Pelton称，由于新形式文件的出现，特别是视频文件的出现，使得用户需要更高的性能，而这一容量可以满足用户的需求。<br />
<br />
目前思科在太空中已经有了一个IP路由器。大约在五年前，思科改进了他们的一个移动接入路由器(Mobile Access Routers)，并将这个路由器通过科学试验卫星送入了地球轨道。Pelton称，思科利用这个路由器进行了试验，不过这个路由器容量很低，并且无法获得24小时工作的电力。<br />
<br />
只有当太空路由器被发射升空后，真实的测试才开始展开。目前该特制设备已经被安装在国际通信卫星公司(Intelsat)的IS-14大型通信卫星上。该卫星计划在今年年底被发射升空。思科称，IS-14卫星原定在今年第一季度被发射升空，但是多方面原因导致在肯尼迪航天中心的发射日程安排被推迟。一旦IS-14卫星进入轨道，美国政府将对路由器展开为期三个月的测试工作，此后众多运营商和私营企业将对该路由器进行为期一年的测试。<br />
<br />
Pelton称，太空路由器并不是由廉价和过时的组件建造而成，设备的每一个组件都可经受大量射线的考验，预计使用寿命为15年，思科则是这一项目中的专用组件供应商。尽管太空极为寒冷，但是路由器的散热依然是一个非常棘手的问题。原因在于太空是一个真空环境，没有空气对流现象，因此路由器上的热量无法被带走。Pelton表示，路由器只能通过安装与太空相连的散热片来散热。<br />
<br />
与此同时，Pelton介绍称，与其它的思科路由器一样，IRIS路由器可以被遥控。由于路由器被发射升空后无法进行维护，因此IRIS路由器安装有备用设备。实际上，IRIS路由器上安装了两个路由器，其中一个作为备份，同时路由器上还安装了两个独立的调制解调设备，以作为备份。整套设备的尺寸大约为24英寸×18英寸×18英寸。整个卫星的尺寸相当于一辆校车那么大，路由器与三组转发器相连，每组由60余个转发器组成。<br />
<br />
思科相信一旦卫星进入太空，路由器的吞吐量将达到100Mbps。虽然这一吞吐量与地面路由器相比性能逊色不少，但是在太空中这却是史无前例的。路由器采用了思科的全互联网工作操作系统(IOS)软件。为了传输加密信息，路由器上还安装了IPSec(IP安全)协议套件。<br />
<br />
对于太空路由器来说，电力供应也是一个不小的问题。尽管这类卫星通过自带的太阳能板可以提供5000至7000瓦电力，但是路由器只能获得其中的一小部分电力供应。<br />
<br />
思科的最终构想是建立起一个由带路由器的卫星组成的大型网络，在卫星之间就可以进行路由。Pelton称：“一旦这一技术成熟，这将成为卫星的主要任务。”尽管目前思科还没有对太空路由器市场究竟有多大，以及将对IRIS投资多少进行评估，但是Pelton称全球卫星市场每年大约为1250亿美元，并且每年都保持着两位数的增长势头。<br />
<br />
此外，Chartrand称，目前各方面都对这一技术非常感兴趣，特别是美国军方。因为美国军方需要在战区内进行低延时通信，而战区究竟在何处谁都难以预料。根据思科的构想，即使地面基站被摧毁，那么由卫星组成的路由网络仍然可以正常工作。<br />
<br />
Chartrand指出，目前美国空军正在大力推动在2019年发射升空空军自己的卫星路由器计划，美国空军的这一举措可以让思科大赚一笔。最后，Chartrand强调称，目前只有当卫星进入轨道完成测试后，外界才能知道思科的卫星梦想究竟有多大的可行性。<br />
