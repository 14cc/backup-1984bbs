																											<div id="postmessage_258572" class="t_msgfont">最近破网软件统统失效，只能再次把注意力转移到TOR上，可是TOR最近也会出现找不到目录的问题。很多人发现了用bridge的方法，我也试了一下，确实比较有效。<br />
Zuola讲的挺有道理的， 网桥对于 Tor 的反屏蔽战役来讲是一个进步。要使用网桥，您必须找到一个网桥地址。一个网桥就足以让您进入 Tor 网络了。找网桥这过程,听起来就像是黑客帝国里找电话亭的情节。<a href="http://tor.zuo.la/bridges.html.zh-cn" target="_blank">http://tor.zuo.la/bridges.html.zh-cn</a>。<br />
<br />
我把大家提到的方法详细描述一下。<br />
1.设置bridge之前，首先要获取一个网桥地址，比较有效、简单的方式是通过邮件的方式获取，目前好像只支持从gmail发邮件获取网桥。<br />
获取网桥的方式是：<br />
用gmail（只能用gmail）发邮件到<a href="mailto:bridges@torproject.org">bridges@torproject.org</a>，标题get bridges，正文一行get bridges，等待回复。<br />
回复的邮件是这样的：<br />
[This is an automated message; please do not reply.]<br />
<br />
Here are your bridge relays:<br />
<br />
bridge XXX.XXX.XXX.XXX:port 14f672b246760fddfe07301b7a38c3e6d986fd75<br />
<br />
有用的部分是ip:port的部分，后面的是校验的部分，可以不管。<br />
2. 把获取到的网桥添加到TOR的配置里面，先进入到如下的界面：<br />
<img src="http://i35.tinypic.com/15qvu6o.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
3.然后点设定，再进入到网络设定里面，勾选“我的isp阻挡了对TOR网络的连接”，<br />
然后在下面添加刚才获取到的ip:port部分，点确定退出。不用重新启动，TOR就会重新连接。<br />
<img src="http://i33.tinypic.com/34y42lx.jpg" border="0" onclick="zoom(this, this.src)" onload="attachimg(this, 'load')" alt="" /><br />
<br />
<br />
用此方法成功带套。<br />
<br />
