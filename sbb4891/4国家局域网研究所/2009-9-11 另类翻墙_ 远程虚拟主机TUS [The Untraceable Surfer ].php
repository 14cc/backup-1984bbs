													<h2>正义战争与政治虚无主义</h2>
						
						
																											<div id="postmessage_451312" class="t_msgfont">正义战争与政治虚无主义 <br />
&nbsp; &nbsp;&nbsp; &nbsp; （作者 韩乾&nbsp;&nbsp;生于1990年，毕业于北京清华附中，现在正复读考大学）<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;来源 无物之阵<a href="http://blog.hanqian.net" target="_blank">http://blog.hanqian.net</a><br />
<br />
2009年的诺贝尔和平奖被授予给美国总统贝拉克・奥巴马。消息一出，世界舆论哗然。大多数人的困惑在于此：一个以世界和平为意旨的奖项，难道可以被授予给当代世界战争的主要发动方――美国――的领导人吗？左派知识分子更是义正辞严地指出，给&quot;帝国主义者&quot;发奖，表明了现代道德的沦丧，更表明了正义向殖民主义投降。<br />
<br />
鉴于此，奥巴马特地在领奖时发表了一番精心准备的演讲，为自己辩护。面对指责，他指出，美国所发动的阿富汗、伊拉克战争，都是&quot;正义战争&quot;(他又补充说，后者可能没那么正义)，而不是殖民主义；因为，塔利班与萨达姆都已成为和平的威胁，如果不立即以战争手段予以制止，势必造成严重后果。另外，美国军队已经尽可能避免让战火波及平民，尽管仍有伤亡，但那确属无奈。无论如何，战争虽然会带来各种灾难，但如果没有必要的战争，后果将更加严重。正是在这种意义上，战争是正义的。<br />
<br />
在支持或反对奥巴马之前，需要澄清的是&quot;正义战争&quot;的概念本身。如果这一概念是有意义的，就意味着存在某些律则，赋予战争以道德性质。换句话说，当我们在谈到战争的&quot;合法性&quot;时，首要问题就是：这种&quot;法&quot;――干脆地说，政治正义的标准――有可能存在吗？<br />
<br />
<br />
我们想必会认为，这种律则当然存在，否则像反对希特勒这样的战争，就不会被公认为是正义的了。然而，在惯常思维中，纳粹一方之所以非正义，是因为它是侵略者――主动对其他国家实施了军事行动，侵犯了后者主权；而英美等国一方之所以合乎正义，则是因为它们的目的是保卫己方主权――或者冠冕堂皇地说，是保卫&quot;独立与自由&quot;。看起来，将主权作为政治正义的标准并没有什么问题，但接下来，我们就会看到一些疑点。<br />
<br />
在这里，发生于二十世纪末的科索沃战争是一个具有代表意义的范例。很明显，当时以美国为首的北约军队，是南斯拉夫国家主权的侵犯者；正是基于这样的逻辑，南斯拉夫政府认为军事干涉是不正当的，知识界也站在了北约的对立面。然而此时，德国哲学家哈贝马斯却为北约出兵辩护。他的理由简单易懂：一，北约的目的是调解，是制止正在发生的惨剧；二，北约的行动是十分审慎的――即使这造成了平民伤亡，但就其&quot;外科手术&quot;式的手段来说，误伤的存在毕竟是可接受的。当然，这一辩护的直接后果是招致了无数的口诛笔伐。<br />
<br />
让我们回顾此次战争的过程。它起源于南斯拉夫境内科索沃地区塞尔维亚族与阿尔巴尼亚族的冲突。两个民族历来不和――南斯拉夫政府有失公平的民族政策难逃其咎――而在东欧剧变之后，矛盾日益突出。阿尔巴尼亚族在被压迫的情况下，试图反抗政府统治，获得独立地位；这招致军队的严厉镇压。阿族武装与塞族武装、阿族平民与塞族平民之间连续数年流血不断，而在南政府的偏袒与支持下，塞族很快取得优势地位，阿族则面临着被灭绝的命运。此时国际社会开始调停，北约发出最后通牒，命令南政府立即停火，但后者认为此举干涉内政，不予理会，反而对阿族实施了大规模种族清洗。屠杀造成三十万人丧生。1999年3月25日，最后时限已过，美国宣布外交努力失败，北约便出动舰队与飞机，以空中打击的形式，瓦解了南政府的抵抗，最终将南总统米洛舍维奇送上国际法庭。<br />
<br />
科索沃战争引起了巨大争议。北约选择以暴力达到目的，这到底是侵略行径，还是正义做法呢？问题并不在于以战争制止屠杀是否合法，而在于当米洛舍维奇选择将&quot;国家主权神圣不可侵犯&quot;的律则作为其屠杀正当性的来源时，和平手段还能够制止惨剧吗？答案应当是明确的。<br />
<br />
所以，从上面的范例来看，即使我们先将以战争手段搁置一边，将主权作为政治正义的标准也是不合适的。仅从国家的历史来看，主权观念与人的财产权观念并不相似：后者是绝对严格而实存的私权，而前者仅是一外铄性概念，是在国―国的国际政治背景下的假设。既然如此，政治正义便不能以国―国的现实关系为基础；与之相反，这种现实关系，恰恰应当以根植于政治本身的、普遍的、绝对的正义为前提。<br />
<br />
在古希腊时代，政治的目的是让人――天生的&quot;政治动物&quot;――过上&quot;良善生活&quot;(good life)。这表明，人只有在符合政治律则、参与政治活动、服从政治决议的情况下才是合乎道德的。古希腊民主政治的特点正在于此：它不仅可以决定城邦公共事务，也可以对个人进行控制。事实上在这一时期，公―私或者说公共―个人的分野还不存在，财产权等一系列个人权利，也付之阙如。例如，从今天的眼光来看，苏格拉底的言行虽然违逆了当时的道德，却还谈不上犯罪；但在古希腊这样一个不存在言论自由的时期，道德―法律―政治决议是三位一体的，个人在其面前必须服从。然而，随着文明的碰撞、宗教的冲突不断发生，(当世界上第一个真正意义上的超级大国――罗马共和国/罗马帝国统治了多个民族时，单一族群便不可能具有法律的地位了；事实上，万民法之所以具有这样的名称，正是因为它超越了特定族群的规范。现代政治的雏形正是出现于此)一种单一道德对人全方位的控制已经难以维系，基督教对整个欧洲的统治即是这种控制的最后环节――很快，随着启蒙运动发生，基督教道德便失去了它的地位。在这里，深刻的变化是道德―法律―政治联盟的解体。不同民族、不同地区乃至不同信仰的人们，不可能具有相同的道德观念；随着视域的扩大，多种文化、多种价值观进入了同一个社会，政治对&quot;良善生活&quot;观念便不再拥有解释权了。这时，公―私或者说公共―私人的观念才真正成为现实。<br />
<br />
既然多元社会使得单一文化不复存在，那么良善生活也就不可能具有唯一的内涵。私人领域的出现意味着：人有权追求他/她所期望的生活而不受公共声音与政治权力的干扰。只要不侵犯他人，个人便可以完全掌握自己。这便意味着个人自由的出现，也意味着政治已经不再和良善生活存在任何实质联系。<br />
<br />
这时，政治的意义又成了关键问题。显然，当个人自由与文化多元成为社会良好秩序的前提时，政治的任务便是维护这一秩序。当政治不能、也无权提供良善生活的标准时，它至少应为个人选择乃至多元社会本身提供稳定可行的架构。在这里，我们看到了与古典政治不同的现代政治；与之相伴的还有作为政治之根的正义标准。<br />
<br />
我们想必都会认同&quot;正常社会&quot;这一概念。它表明，存在着某些共同而普遍的律则，当一个社会违反这些底线时，便不能被称为&quot;正常的&quot;，其政治也不能被认可为正义的。这一标准即是：社会能否为个人选择与多元文化共存提供足够大的空间；个人能否有足够的自由选择他/她所期望的生活，以及诸种文化、诸种价值观是否可以得到公正的对待、是否得以免遭政治权力的干涉。换句话说，当政治能够为上述标准提供保障时，才是正义的。这正是我们将希特勒德国、塔利班阿富汗与萨达姆伊拉克成为&quot;非正义&quot;的原因；这也可以解释，为什么北朝鲜、古巴和伊朗不是侵略者，却还被认为是(按照美国的话说)&quot;邪恶国家&quot;。尽管对非正义国家采取战争手段仍有巨大争议，但仅从结果来看，正常社会在德国、阿富汗和伊拉克的复兴或初现依然是令人高兴的。<br />
<br />
为某一场战争(尤其是以遏止莫须有的&quot;大规模杀伤性武器&quot;为由而发动的伊拉克战争――很难说这是正义战争)进行&quot;评估&quot;是无比困难的，也不是此篇文章的任务。我想争辩的是，将主权、地域(西方/东方)、国力(强大/弱小)、甚至人种(白人/有色)作为政治正义的标准是完全不能接受的：这是一种政治虚无主义观念，它可能会为各式各样的威权统治提供合法性。一个流行看法是：欧美对非欧美国家的战争是殖民战争。在这种看法中，欧美―西方―强国―白人被同化为一方，而非欧美―东方―弱国―有色人种被同化为另一方，前者总被成为&quot;殖民者&quot;或&quot;霸权主义&quot;，而后者则被看成是反抗霸权的英雄，或者是非西方价值观的保卫者。在这里需要澄清的是：正常社会的标准与某种特定价值观或文化并没有任何关系；正常社会意味着存在一个开放而公正的架构，使得不同文化得以良好共存。价值观论者没有看到如下事实：在日本、韩国、台湾和印度这样的正常社会中，其传统价值观并未因社会架构的转型而受到损害，所谓西方价值观也并未趁机取得强势地位。<br />
<br />
还有人争辩说，战争严重损害了后者的国家利益。这与政治正义的主权标准大同小异――事实上，当主权被拿来作为侵害人权的挡箭牌时，国家利益也成了侵害公民利益与社会利益的保护伞。&quot;国家利益&quot;是一个相当抽象的概念――当苏联成为超级大国时，它的国家利益指的是什么呢，是那些核弹头，是枪支与坦克，还是各种垄断部门？会有人为这些利益的丧失而不满吗？更重要的是，非正常社会中脆弱的公共利益没有得到重视，沦为统治者私产的&quot;国家利益&quot;却有人为之痛心疾首，这不得不令人概叹。<br />
<br />
虚无主义的政治正义标准之所以是危险的，在于它倒转了国家与个人的关系。国家合法性的来源是公民契约，政治正义的标准与它应当有深刻的联系。然而，在主权或利益论中，我们看不到哪怕一丁点个人的影子；正义仅来自于国家事实统治的现状，而这种统治本身是否正当，没有被当成问题。在如此范围的政治中，个人退隐了；正义非但来自个人―国家的法，反而来自权力存在的既成事实。但是，权力可能正当，也可能非法；可能被充分限制，也可能肆意妄为。米洛舍维奇、希特勒与萨达姆等人的权力，如果被这种虚无主义观念保护起来，后果只能是惨剧。<br />
<br />
回顾历史，以国―国关系为基础的国际政治，很长时间以来都是暴力与利益的演出舞台。然而，尽管国家存在在事实上来自暴力，但其正当统治的根源还是来自法(契约)。这便意味着，只要法的精神存在，政治就不会被虚无主义观念把持。政治正义就根植与个人―国家的关系中。而其之所以重要，则在于它对个人―国家―世界的现代政治图景作出了规划。好的政治应当是承载个人―世界关系的框架，国家则应该成为其中最重要的工具。因此，合乎正义的政治必须使得国家成为个人自由与文化多元的支持者；这种要求深植于人类历史，它超越了狭隘的国际政治语境，而为个人―世界政治提供了可能。 <br />
