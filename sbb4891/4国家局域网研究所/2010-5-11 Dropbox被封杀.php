													<h2>William Long：纪念Dropbox君</h2>
						
						
																											<div id="postmessage_609819" class="t_msgfont">一 <br />
中华民国九十九年五月十日，就是Dropbox君遇害的那一天，我独在墙外徘徊，遇见推友们，前来问我道，“先生可曾为Dropbox君写了一点什么没有？”我说“没有”。他们就正告我，“先生还是写一点罢；很多人都是听先生的推荐才知道Dropbox君的。” <br />
<br />
这是我知道的，凡我用过的网络存储服务，大多都不太好用，时间长了就甚为寥落，然而在这样的生活中，毅然将Dropbox放入我电脑“启动”组里的软件，这几年来就只有她。我也早觉得有写一点东西的必要了，倘使我能够相信真有所谓“在天之灵”，那自然可以得到更大的安慰，――但是，现在，却只能如此而已。 <br />
<br />
可是我实在无话可说。我只觉得所住的并非人间。放在Dropbox里数百兆的工作和学习文档都再也无法访问，使我艰于呼吸视听，那里还能有什么言语？长歌当哭，是必须在痛定之后的。而此后几个五毛的阴险的论调，尤使我觉得悲哀。我已经出离愤怒了。我将深味这非人间的浓黑的悲凉；以我的最大哀痛显示于非人间，使它们快意于我的苦痛，就将这作为后死者的菲薄的祭品，奉献于逝者的灵前。 <br />
<br />
二 <br />
真的猛士，敢于直面惨淡的人生，敢于正视淋漓的鲜血。这是怎样的哀痛者和幸福者？然而造化又常常为庸人设计，以时间的流驶，来洗涤旧迹，仅使留下淡红的血色和微漠的悲哀。在这淡红的血色和微漠的悲哀中，又给人暂得偷生，维持着这似人非人的世界。我不知道这样的世界何时是一个尽头！ <br />
<br />
我们还在这样的世上活着；我也早觉得有写一点东西的必要了。Dropbox被封已经过了一天时间了，忘却的救主快要降临了罢，我正有写一点东西的必要了。 <br />
<br />
三 <br />
在无数被封网站之中，Dropbox是我的最爱。一个划时代的云存储服务，我向来是这样评价她。她不是“苟活到现在”的无良网站，而是一个无辜被封杀的网络存储服务。 <br />
<br />
Dropbox的名字第一次为我所见，是在去年Gmail的一封邮件里，我的一个好友发邮件推荐我注册这个服务，就是Dropbox；但是我注册后发现需要安装客户端，令我踌躇不前。直到后来，越来越多的朋友发来邮件邀请我注册，才令我感到诧异，这到底是个什么服务，居然有这么大的魅力，后来一个好友告诉我，说：这就是云存储服务。其时我才恍然大悟，原来这个服务能实现多台电脑上文件的自动同步和更新，心中却暗自诧异。我平素想，为了同步文件，无论如何也需要用U盘将文件拷贝粘贴，总有些麻烦，而使用Dropbox竟然可以自动完成这所有的更新操作，实在节省了我大量的时间，于是我使用Dropbox的时间就多了起来，直到将其加入“启动”组，成为我电脑必备的软件，虽然中途也见过微软的Mesh等同类软件，到总觉得速度和性能Dropbox最优，直到昨日Dropbox突然被害，才发现在我的记忆上，昨日的Dropbox就是永别了。 <br />
<br />
四 <br />
我在十日的早晨，还发文章向网友推荐Dropbox，并分享了阮一峰翻译的“Dropbox的创业经历”的PPT；下午便得到噩耗，说Dropbox居然被封，连https都无法访问，而客户端一经退出就再无法登录。但我对于这些传说，竟至于颇为怀疑。我向来是不惮以最坏的恶意，来推测中国的网监部门，然而我还不料，也不信竟会下劣凶残到这地步。况且始终没有任何意识形态的工具软件Dropbox，更何至于无端在GFW门前喋血呢？ <br />
<br />
然而即日证明是事实了，作证的便是访问Dropbox出现的“连接被重置”的截屏。而且又证明着这不但是封杀，简直是定点封杀，因为所有包含dropbox.com的链接都无法打开。 <br />
<br />
但现在网监部门就有令，说她是包含“有害信息”的工具！ <br />
<br />
但接着就有流言，说她是被“海外敌对势力”利用的。 <br />
<br />
惨象，已使我目不忍视了；流言，尤使我耳不忍闻。我还有什么话可说呢？我懂得衰亡民族之所以默无声息的缘由了。沉默呵，沉默呵！不在沉默中爆发，就在沉默中灭亡。 <br />
<br />
五 <br />
但是，我还有要说的话。 <br />
<br />
我没有亲见；听说她，Dropbox，是一群创业者的创新之作，深受用户欢迎，依靠用户的口碑传播而获得了成功，一个网络存储工具，稍有人心者，谁也不会料到有这样的结果。但竟在中国被封杀了，先封杀的是其文件分享域名dl.dropbox.com，已经使得用户分享的文件无法从网上访问，只是Dropbox客户端还没有便死，同时使用https的方式还可以访问，但是现在dropbox.com的域名进入了黑名单，IP地址也被封锁，这两种方式无疑是致命的，Dropbox终于是死掉了。 <br />
<br />
始终没有任何意识形态的网络存储工具Dropbox确是被封杀了，这是真的，有“连接被重置”的截屏为证；沉勇而友爱的Twitter君也死掉了，有她的截屏为证；只有一样沉勇而友爱的Google君还在香港的医院里呻吟。当三个为网民服务的工具网站从容地转辗于网监部门所发明的金盾工程的包围中的时候，这是怎样的一个惊心动魄的伟大呵！中国政府宣称“中国的互联网是开放的”，“中国政府鼓励互联网的发展”的夸夸其词，不幸全被GFW抹杀了。 <br />
<br />
但是中国的网监部门却居然昂起头来，不知道个个脸上有着血污……。 <br />
<br />
六 <br />
我已经说过：我向来是不惮以最坏的恶意来推测中国人的。但这回却很有几点出于我的意外。一是当局者竟会这样地凶残，一是监管部门竟至如此之下劣，一是不作恶的网站临难竟能如是之从容。 <br />
<br />
苟活者在淡红的血色中，会依稀看见微茫的希望；真的猛士，将更奋然而前行。 <br />
<br />
呜呼，我说不出话，但以此记念Dropbox君！ <br />
