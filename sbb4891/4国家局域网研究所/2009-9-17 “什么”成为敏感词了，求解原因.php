													<h2>“什么”成为敏感词了，求解原因</h2>
						
						
																											<div id="postmessage_245763" class="t_msgfont">【按】这是今早在车上看到的一个帖子，转发在了GFW Blog上的。一看到“什么”二字我就想起了焦点访谈刚播不久的《六个为什么》，让人意想不到的是，在Google.com上搜索“什么”二字居然会撞墙了，这封锁也太没有逻辑了吧。<br />
<br />
更正：将“什么”封锁的原因与《六个为什么》相联系，这纯粹是鄙人一个想当然的臆断，因为鄙人没有搜索到更多的信息加以了解，所以以至于凭借一篇技术文章就确定封锁原因，这是辨别真伪下判断时，相当不好的习惯性思维，据组员yangzhe1990介绍，“什么”二字早在9月初就被阅后即焚、google连坐时封锁了，恩，这里无知了，道歉，声明，但转帖并不是有什么邪恶意图，这里做分享与讨论的由头，按语不删，以作警示。<br />
<br />
来源：<a href="http://www.mitbbs.com/article/ChinaNews/31986535_0.html" target="_blank">www.mitbbs.com/article/ChinaNews/31986535_0.html</a><br />
<br />
是的，你没看错，现代汉语中最常用的词语之一&quot;什么&quot;已经成为了新晋GFW关键字。<br />
<br />
具体的形式是以&quot;.google.com(*)什么&quot;的形式按URL关键字过滤的。为了打消某些同学对技术的顾虑，我们不妨做如下几个试验：<br />
<br />
1. 打开<a href="http://www.google.com" target="_blank">http://www.google.com</a>。请注意一下你的浏览器有没有自动重定向到<a href="http://www.google.cn" target="_blank">www.google.cn</a>：因为阉割版的google.cn被视作国内网站，不经过GFW的URL屏蔽的。如果确实被重定向了，请使用<a href="http://www.google.com/ncr" target="_blank">http://www.google.com/ncr</a>。好，现在我们已经进入Google主页了。试着随便搜索一个什么东西，比如&quot;test&quot;，你应该能得到正常的搜索结果以确定网络工作正常。接下来我们试着在搜索框中键入神奇的咒语&quot;什么&quot;，点击搜索：这是返回得便不再是正常的搜索结果，而是 &quot;到服务器的连接被重置&quot;之类的。接下来你会发现你甚至无法访问刚才的google.com主页：这就是GFW著名的撞墙后强制眼保健操。别担心，一分半钟后就会恢复的。如果你有听包工具的话，还可以看到大量的伪造RST包向你发来，这就是GFW所为。<br />
<br />
我们都是受过正统科学训练的人，为了排除Google本身故障的原因，我们再做如下对照实验。<br />
<br />
2. 打开<a href="http://www.gnu.org" target="_blank">http://www.gnu.org</a>。这是著名的开源组织GNU的首页，教育网的同学注意连接国际网。现在我们试着在网址后面随便敲一些东西，比如<a href="http://www.gnu.org/dskjfslkd" target="_blank">http://www.gnu.org/dskjfslkd</a>，这是应该会看到正常的404页面：这里说的正常的404页面是指<a href="http://www.gnu.org" target="_blank">www.gnu.org</a>本身的404页面，上面有网站自定的信息和管理员联系方式。为了说明GFW的工作方式，我们试着访问<a href="http://www.gnu.org/www.facebook.com" target="_blank">http://www.gnu.org/www.facebook.com</a>。因为&quot;www.facebook.com&quot;是GFW的的URL关键字(就是说所有包含<a href="http://www.facebook.com" target="_blank">www.facebook.com</a>的网址都会被屏蔽，这就是为什么我们上不了facebook的原因之一，也可以用刚才的Google方法来测试)，所以连接会被GFW所RST，这是将看不到正常的404页面(取代的是浏览器的错误页面)，并且<a href="http://www.gnu.org" target="_blank">www.gnu.org</a>的主页将会无法访问一分钟，同时可以监听到GFW伪造的RST包。稍等一会，我们等待gnu主页恢复正常试着访问<a href="http://www.gnu.org/.google.com" target="_blank">http://www.gnu.org/.google.com</a>什么，你会发现和刚才的症状一模一样：.google.com(*)什么确实是一个GFW的URL关键词。<br />
<br />
有好奇的同学会问，你说的关键字中间为什么有一个(*)呢？情况是这样的：原本的GFW的URL屏蔽仅仅屏蔽关键字本身，就是只要包含关键字的网址就会被屏蔽，类似的例子除了上面举的&quot;www.facebook.com&quot;, &quot;www.twitter.com&quot;还有诸如&quot;www.googlevideo.com&quot;之类的，这些都可以用上面的方法逐一验证。可是通过访问<a href="http://www.gnu.org/" target="_blank">http://www.gnu.org/</a>什么我们可以发现，&quot;什么&quot;这个词本身并不是一个GFW关键字。&quot;什么&quot;仅仅当它和&quot;.google.com&quot;组合在一起的时候才会成为关键词，也就是上面所描述的通配符(*)：&quot;.google.com&quot;和&quot;什么&quot;之间有任何东西都会被过滤，也就是不能在google.com中搜索诸如&quot;xx是什么&quot;这样的东西。<br />
<br />
这种wildcard matching是GFW的新技术，根据昨晚到现在的观察应该还在技术试验阶段，时而生效时而不生效。下面我们来说说这个新技术为什么可怕。<br />
<br />
除了不知道GFW的是什么的同学之外，很多人都对GFW持轻蔑态度，原因是他们认为GFW本身没什么，&quot;我会翻墙，轻松着呢&quot;。但是他们恰恰忘记了，GFW 从来就不是为少数人设计的。只要让大多数不会翻墙或者懒得翻墙或者害怕翻墙的人远离高墙不想让他们看到内容，GFW就成功了。反过来，GFW通过一种不确定、无规章的方式(没有法律条文支持GFW，没有公开国家机关承认GFW，没有公开的关键词列表)，让大家生活在一种恐慌之中，慢慢的让每个人习惯于自我审查，在心中构建自己的墙。<br />
<br />
而这里的问题在于，如果墙的封锁太过分以至于影响的人们的正常工作学习生活，那么大家不但都会知道墙的存在，还会去试图翻墙，这样GFW就彻底失败了：这样就和拔网线或者变朝鲜没有区别了。绿坝显然是逾越了这一底线，所以大家反映激烈。而最近对 youtube/facebook/twitter等主流国外网站的封锁虽然让大家很难受，但是显然还没有超过阈值。通过之前的事情，我们和他们都发现了由于Gmail、Google Docs和Google搜索本身在大家生活中的地位，虽然他们无比痛恨Google(因为其自由传播特性和https加密令GFW的全文报文过滤没有用武之地)，他们还是不敢冒着民意沸腾的风险全面封锁google.com。而如果有了wildcard matching这样强大的工具，GFW就可以做到定点清除：仅仅不让你在Google中搜索某些特定关键字，而又不影响Google.com的相对正常使用：比如这个&quot;什么&quot;可能是个测试，将来可能会封禁&quot;.google.com(*)什么||GFW&quot;类似的东西，禁止大家通过Google来了解什么是 GFW。再好比Wikipedia上要好多真理部不喜闻不乐见的条目，而若是断然封禁.wikipeida.org(就像几年前那样)，很多通过Wiki 来学术的人(比如刘雨晨)一定会疯掉并且对GFW痛恨至极。而如果有了现在的工具，就可以定点清除wiki中的某些条目(这个工作现在是通过TCP报文全文分析做的，很耗费资源)而不引起过大的民愤了。<br />
<br />
总之现在的大众网民就像是被慢慢加热的水煮熟的青蛙一样，慢慢的麻木，慢慢的被控制。<br />
