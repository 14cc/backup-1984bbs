													<h2>不用BT还能用什么？带你纵览p2p文件共享软件</h2>
						
						
																											<div id="postmessage_364195" class="t_msgfont">原帖作者：黄果皮<br />
<br />
一个小小的禁令就让广大网民惶恐不安，直呼没有BT不能活，让不少资深网民觉得好笑，管天管地还能管BT，它能管得了别人用什么吗？从Napster开创p2p文件分享以来，各式各样的p2p网络和软件层出不穷，在法制严明的国家和地区，大家就不能p2p分享了吗？答案是否定的，看看著名的开源社区网站sourceforge.net上面，最热门的前50个里面有很多都是p2p文件分享程序(Peer to peer Filesharing Program)，有eMule,Ares Galaxy,DC++,Shareaza,MLdonkey等等，现在就带着大家看看。<br />
<br />
BT作为本质上的在线发布程序，严重依赖web发布站点，这是他的致命弱点，客观地说，BT只是一个准p2p文件分享程序，而传统的p2p文件分享程序其实都是不需要所谓的web发布站点就能使用的，他们遵循着“共享-&gt;搜索-&gt;下载”的传统模式，有很多根本就没有链接和种子的概念，不依赖于web发布，有客户端你就能自由共享，搜索和下载任意资源，而不用到web网站上去找，这反而增强了他们的生存能力。<br />
<br />
eMule，也就是我们常说的电骡，sourceforge下载量排名第一，已经超过了5亿，德国人Merkur（真名据说叫Hendrik.Breitkreuz）当年创造emule的时候，正是美国商业公司Metamachine所创造的edonkey2000文件分享网络及其客户端eDonkey也就是我们常说的电驴最火的时候，但是开源的电骡很快就击败了ed2k的老祖宗官方电驴客户端，而倒霉的电驴公司也在2005年与RIAA的版权官司中败下阵来，赔款3000万直接完蛋。而开源非商业的eMule不仅没有官司之忧，更在开源社区emule-project的管理下茁壮成长，最终击败当时的老大Kazaa，成为至今全球最流行的文件分享客户端。如今eMule更有很多修改版（mod）出现，甚至在sourceforge前50中，我们还能看到eMule Xtreme和eMule MorphXT这两大官方社区的eMule mod的身影。<br />
eMule支持的p2p网络有KAD和ed2k，其中KAD网络是无服务器模式，这意味任何关停服务器的打击方式对KAD网络都无效。链接系统支持：ed2k连接。搜索文件功能也不含糊，支持布尔运算符，通配符和各种文件参数。更可以通过消息和好友系统与其他人联系，查看他人的共享文件夹，集成了IRC聊天室，其他特点有自定义皮肤外观样式，支持过滤恶意IP，定时计划任务，下载文件智能自动分类，基于web的异地远程管理服务等，一些mod支持以系统服务方式运行（无界面后台运行）。(注：linux下有aMule）<br />
<br />
eMule官方网站：<a href="http://www.emule-project.net/" target="_blank">http://www.emule-project.net/</a> ，小心，eMule的假冒官方网站有很多域名诸如：emule.com，emule.org之类，不要被这些钓鱼站点欺骗了。<br />
<br />
Shareaza，另一款著名的p2p分享程序，在2002年，迈克尔・斯托克斯发布了一个新版Gnutella客户端，他将之命名为Shareaza。先来说说Gnutella，Gnutella也是一种无服务器的p2p文件分享网络，其在p2p领域内曾占有超过40%的份额，Gnutella网络的第一个客户端由Nullsoft公司的贾斯汀・法兰科（Justin Frankel）与汤姆・帕勃（Tom Pepper）于2000年早期最先开发。人们普遍认定法兰科与帕勃在开发Gnutella项目的时候吃了许多的Nutella（一种意大利巧克力榛子酱）。Shareaza的作者斯托克斯基于Gnutella重写了协议，他称之为Gnutella2。<br />
Shareaza支持的p2p网络不仅有Gnutella和Gnutella2，还支持eDonkey（电驴）网络和BitTorrent，链接系统支持：ed2k和magnet。Shareaza的特点有：下载预览播放、用户意见、评分，IRC聊天、传送消息，自定义皮肤外观样式，以及3个用户模式，安全性黑名单，可过滤恶意IP与客户端。(注：linux下有Sharelin)<br />
Shareaza官方网站：<a href="http://shareaza.sourceforge.net/" target="_blank">http://shareaza.sourceforge.net/</a> <br />
<br />
Ares Galaxy，其口号为“Filesharing made easy”，Ares是一个类似Gnutella的很普及的p2p网络，其标准（和最普及的）Ares Galaxy客户端于2005年3月是美国P2P客户端中第六常用的。链接系统支持：arlnk链接。此外还支持文件共享聊天室，强大的文件库管理功能，内建多媒体播放器方便预览下载中的音乐和视频。<br />
Ares Galaxy官方网站：<a href="http://aresgalaxy.sourceforge.net/" target="_blank">http://aresgalaxy.sourceforge.net/</a><br />
<br />
DC++，支持p2p网络为Direct Connect和Advanced Direct Connect (ADC)网络，Direct Connect是一种常用于大学内部网络中的协议，DC++在Direct Connect文件分享网络中占据90%的位置。链接系统支持：magnet链接。<br />
DC++官方网站：<a href="http://dcplusplus.sourceforge.net/" target="_blank">http://dcplusplus.sourceforge.net/</a><br />
<br />
MLdonkey，最强大和最另类，2001年法国人Fabrice Le Fessant使用OCaml语言编写，同时有些部分使用了一些C语言以及汇编语言的代码，开发出来了MLDonkey，起初也是为了在Linux下替代eDonkey（倒霉的电驴）。MLdonkey以C/S结构设计，后台的下载内核可以通过内置的Telnet以及Web界面操纵，也可以使用各种图形前端程序。Mldonkey的特点在于“全能”，最全能的操作系统的支持，最全能的p2p网络支持。<br />
Mldonkey有多么全能可以到其官方wiki查阅：<a href="http://mldonkey.sourceforge.net/Main_Page" target="_blank">http://mldonkey.sourceforge.net/Main_Page</a><br />
<br />
Perfect Dark，作为版权保护最严格的国家，日本的p2p网络开发者一开始就关注的是匿名性，Perfect Dark也是一个匿名p2p网络的客户端，作为Winny和Share的替代者，Perfect Dark被认为是次世代P2P，开发者为“会长”，支持完全的无服务器匿名p2p网络，就在人们以为Perfect Dark也会停止开发时候，2009年10月Perfect Dark刚刚发布了新版，目前语言有日本和英文，有爱好者制作中文汉化。对于爱好日本动漫类资源的人士来说是个好消息。<br />
<br />
值得注意的是，上述大部分都是开源的非商业的软件，和Napster，Kazaa，电驴（edonkey）等不同的是没有任何商业公司控制，这意味着消除了商业公司软件所带来的集中垄断的风险，这些开源项目中一个最常见的口号是“它是完全免费的，也决不包含广告软件、间谍和流氓软件。我们之所以创造它是为了快乐和知识，而不是为了金钱。”只要有人有兴趣参与，这些软件就可以继续生存下去，而不论何时何地。<br />
<br />
最后就算你离不开BT，你完全可以想办法加入一些PT（Private Tracker），当然相比那些最近倒掉的Public Tracker有一些门槛，甚至由于中国流行使用某种商业吸血客户端导致中国人名声不太好，而致一些国外PT站一刀切的封掉中国地区的用户。当然国内也已经有不少成熟和壮大的PT站点，这里就不再累述。（注：PT大都禁止使用迅雷、快车等）<br />
<br />
综上所述仅仅是几种p2p文件共享网络中的几种客户端，实际上还有很多比如：KCeasy，Soulseek，FrostWire等等（这里有一个列表：几十种文件共享应用程序功能对比<a href="http://emule-fans.com/comparison-of-file-sharing-applications/" target="_blank">http://emule-fans.com/comparison-of-file-sharing-applications/</a>），更有传统的IRC和Usenet新闻组的途径进行文件分享。当Napster的创始人美国大学生Shawn Fanning在他的大学寝室里为我们开启p2p文件共享时代时，就不再有人能彻底关闭这个通往自由分享世界的大门。<br />
