													<h2>分享：FireFox好用的插件</h2>
						
						
																											<div id="postmessage_367844" class="t_msgfont">从05年开始使用FireFox以来，丰富的插件资源一直是我坚持使用FF的理由。<br />
<br />
至于现在争议很多的插件安装过多后启动软件速度变慢问题，我认为是个伪问题，现在用户对互联网依赖极高，浏览器可能是唯一一个一天都不用关闭的软件。也就是说FireFox一天可能才打开一次，这种情况下，启动FireFox是5秒还是10秒有多大的意义呢？<br />
<br />
现在我的FireFox 3.5.5安装了18个插件，在Dell D630（升级为3G内存）+win7 64bit系统环境下的启动速度是6-8秒左右，IE8大约是4-5秒。Chrome和Opera更快，大约3秒左右就启动了。但FireFox是我使用时间99.9%的浏览器，IE8为0.01%，Chrome和Opera就是用来出了新版本升级用的。<br />
<br />
看新闻Chrome的插件系统也开始完善，我看好Chrome的未来，可能2年后我就改投Chrome阵营了――如果FireFox放弃发展的话。<br />
<br />
用过大约3个月时间的64bit版FireFox，感受不到传说中与64bit系统的完美交融，64bit FireFox在64bie win7中的稳定性和32bitFireFox版本并无使用上的区别。而且Adobe一直没有开发for windows的64bit Flash插件，虽然Flash广告没了，但看论坛里博客上的一些内嵌音频视频也非常麻烦，同时IE tab插件在64bit版本的FireFox中也是失效的。<br />
<br />
上个月开始重新使用32bit版本FireFox。<br />
<br />
我在mozilla.org上的附加组件收藏夹地址是：<a href="https://addons.mozilla.org/zh-CN/firefox/collection/mayimayi" target="_blank">https://addons.mozilla.org/zh-CN/firefox/collection/mayimayi</a> 下面提到的插件这个地址都可以下载到。<br />
<br />
下文种插件的排序是按喜好程度<br />
<br />
NO.1当然是Adblock Plus<br />
<br />
让网页上的各种广告销声匿迹的最佳选择。我经常直接用Adblock Plus来屏蔽那些广告“帧”，一下子网页就干净了。<br />
广告商和广告联盟的最恨！<br />
<br />
NO.2 AutoPager<br />
<br />
可以自动加载翻页。非常非常实用，为了提高PV，网站常常把一篇文章分成无数页面，不得不逐一点开，有了AutoPager就可以在一个页面下看完全部文章了。<br />
网站编辑的最恨！<br />
<br />
NO.3 Foxmarks<br />
<br />
在多台PC上使用同一个收藏夹的利器。我们的记忆越来越弱，收藏夹的作用越来越大。收藏夹在云端同步的好处就是随时随地的同步。Foxmarks也有IE版，但这是我几乎用过最烂的体验，还是让FF归FF，IE归IE吧。<br />
<br />
注：Foxmarks现在被墙了，同步时需翻墙。<br />
<br />
NO.4 AutoProxy<br />
<br />
杏花最爱，GWF的最恨。如图标所示，谁让你出生在中国是不幸，但FireFox装了AutoProxy这个插件就是“福”――这个感觉真让我舒服，它让我忘了我没地儿住。需配合翻墙软件使用。<br />
<br />
NO.5 Echofon for Twitter <br />
<br />
常用Twitter用户的极好插件。在FireFox状态栏就可以使用Twitter。注：Twitter已墙，需要配合翻墙软件使用。<br />
<br />
NO.6 Gmail Manager<br />
<br />
Gmail用户的福音。在FireFox状态栏就可以查看Gmail邮件信息，省却开打开web页面的麻烦。<br />
<br />
NO.7 Evernote Web Clipper<br />
<br />
大爱呀！及其方便在PC+web+Sever+iphone之间同步各种信息，图文影音并茂，支持Html。猛烈推荐！Evernote有其它智能手机版本。<br />
<br />
NO.8 Easy DragToGo<br />
<br />
拖拽超链接。懒到连按鼠标滚轮都不愿做的必备。对没有中键的笔记本也福音。<br />
<br />
NO.9 All-in-One Gestures<br />
<br />
鼠标手势。现在屏幕越来越大，分辨率越来越高，鼠标的屋里移动距离越来越长，懒人们需要这个插件。几乎不用移动鼠标就可以执行各种操作，前进后退停止关闭打开刷新下一个标签页上一个标签页～功能超强大，强大到我只用默认设置就足以应付日常使用。<br />
<br />
NO.10 DownThemAll! <br />
<br />
比自带下载管理好用，比迅雷等第三方稍差的下载管理插件。<br />
<br />
NO.11 IE Tab<br />
<br />
偶尔还是要用到的，省得单独开一次IE。如果能调用使用这个插件前的页面Cookit就更省事，更方便了。<br />
<br />
NO.12 ImTranslator<br />
<br />
在线翻译插件。调用Google Translate，比单独开Google Translate页面要方便很多，用右键选中需要翻译的词、句，然后弹出窗口翻译，会自动识别源文字。稍有不足就是弹出窗口有点点慢，大约7-8秒，能提高到3-4秒就完美了。<br />
<br />
NO.13 TinEye<br />
<br />
经常看到一些好玩的图，但质量太小，想搜一下有没有质量更好的图，这是最方便的办法。<br />
<br />
NO.14 Forecastfox<br />
<br />
天气预报，一直用它。<br />
<br />
NO.15 豆瓣助手<br />
<br />
这个插件非常有意思，在豆瓣电影/音乐/图书等页面上的介绍图片下出现一个搜索按钮，直接可以转向各大搜索引擎和资源下载站<br />
<br />
豆瓣装B青年最爱<br />
<br />
NO.16 Delicious Bookmarks<br />
<br />
FireFox自带的书签管理一直很弱，必须加个插件来增强下。<br />
<br />
NO.17 Tab Mix Plus<br />
<br />
FireFox3.0后自带的标签页管理功能已经变强，Tab Mix Plus可装可不装了，高级功能也懒得去研究。<br />
