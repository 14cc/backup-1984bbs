卫星接收机十忌 <br />
一忌用无质量保证的ＬＮＢ、切换（功分）器和电缆。因一旦内部有短路，则将造成卫星接收机给ＬＮＢ馈电的电源损坏。 <br />
二忌用射频寻星。因一旦频偏（ＴＶ接收机或卫星接收机的调制频偏）或卫星接收机电源干扰，ＴＶ接收机则无法收到微弱的卫视信号，而导致寻星失败。 <br />
三忌收视未开锁频道的加密信号。若加密信号为未开锁频道，一旦搜索它，轻则显示“无可用频道”，重则造成“死”机，或记忆紊乱，或搜索不停。 <br />
四忌接收模拟信号。因模拟信号无“符号率”数据。故当“符号率”为“０”时进行搜索，也将造成“死”机。 <br />
五忌将“ＰＩＤ”码全部设置为“Ｏ”。当“ＰＩＤ”码全部设置为“Ｏ”后搜索时，机内标准“ＰＩＤ”码跟踪电路失去作用，也会造成“死”机。 <br />
六忌用“ＣＨ±”键快速换台。用“ＣＨ±”键快速换台时，会使机内电脑来不及“运算”，将会导致“死”机。 <br />
七忌在无新增频道前，全部删除厂家预置频道。当全部删除厂家预置频道后，将使机内节目数据库空白，轻则整机工作程序紊乱，重则非厂家处理不可。 <br />
八忌胡乱用密码锁台（对有密码锁台的机子而言）。胡乱用密码锁台，一旦忘记，将给你的正常使用带来麻烦。 <br />
九忌与接收射频信号的ＴＶ机放在一起。当与接收射频信号的ＴＶ机放在一起时，卫星接收机内电源的电磁辐射或调制信号会干扰ＴＶ机收视（即画面会出现网纹）。 <br />
十忌在雷雨天气使用。因数字卫星接收机的电路结构较复杂，工作于低压、微功耗状态。抗高压、大电流冲击能力较为脆弱，很容易被感应雷电损坏。特别是无交流关机功能的机子（例如佳讯ＤＶＢ－３０００型），最好在雷雨天气时，把电源插头拔下。 <br />
