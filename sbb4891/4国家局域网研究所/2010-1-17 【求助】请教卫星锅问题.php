【防止Ku信号雨衰】 <br />
想毕您的器材已经安装好了，而且也正在收看，是不是下雨的时候信号不好，有马赛克。那您要了解一下，下面的话题。 <br />
Ku信号的雨衰问题是卫星电视节目接收中经常碰到的问题。无论大雨小雨，或多或少都要影响Ku信号节目的正常收看。最近一段时间，我国大部分地区为多雨天气，给接收Ku信号的单位或个人带来了麻烦。经笔者多次试验和实践，总结出了以下几种方法，均可有效避免Ku信号的雨衰现象。 <br />
<br />
（1）给ku高频头穿上“防雨衣”<br />
我们可以将干净的“可乐”塑料瓶（最好是无色透明的）的瓶口，剪得与Ku高频头一样大小，然后将它套在Ku高频头上，并在塑料瓶下沿打几个滴水孔，防止雨水倒流。这样，即使下大雨也溅不到高频头盖子上，Ku信号便能正常接收和收看了。 <br />
（2）给Ku高频头换上“防雨帽”<br />
大家使用的Ku高频头，上面的塑料盖一经下雨，雨水便会沾在盖子上，影响信号的正常接收。我们可以把Ku头上的塑料盖取下，换上冰箱里常用的保鲜膜，用橡皮筋或透明胶带扎上。因保鲜膜不浸水，故下雨时也能正常收看，在晴天还能略为提高Ku信号的接收强度。 <br />
（3）给Ku高频头“翻翻身”<br />
正常安装Ku天线时，是把天线上的曲杆放在下面，使高频头口朝上对准天线面的。一下雨，雨水便会打湿Ku头上的塑料盖。其实，我们可以把Ku天线倒过来安装，让曲杆放在Ku天线的上部，使高频头口朝下，这样下雨时便能避免雨衰问题。这样处理的唯一缺点是其仰角太大。 <br />
（4）给Ku天线造“防雨屋”<br />
由于接收Ku信号的天线口径大多在0?75m～0.9m之间，其体积较小。因此，可以用铝合金做一个刚好能装下天线的玻璃小屋，小屋最好安装3mm～5mm厚的无色透明玻璃。笔者将天线放进自制的封闭玻璃小屋进行试收，其接收效果丝毫没有受到影响。 <br />
