一、一锅双星<br />
先看看100.5+105.5的双星接收吧（转贴） <br />
由于受接收条件限制，如何运用现有条件和器材进行一锅多星的接收，正成为卫视烧友的追求和梦想。笔者从各种有关刊物上看到了许多一锅多星的接收经历，了解到许多烧友兼收高频头的位置的摆放产生很多疑问和争议，结合本人的接收实践，笔者绘制了卫星波束反射图，和广大烧友谈一点自己的观点和看法。 <br />
<br />
１、一锅多星的接收最好选择信号比较弱的卫星作为主收，信号强的卫星作兼收会省力些。例如：笔者用１．５米中卫天线收１０５．５°Ｅ和１００．５°Ｅ的双星，以１００．５°Ｅ星作为主收，根据反射焦点偏移，偏焦收１０５．５°Ｅ星的高频头在主焦的左面约３公分的位置（紧挨主焦高频头如图），馈源略微向外一点，这样，不仅１００．５°Ｅ星的所有信号全下，和单收１０５．５°Ｅ星差不多。 <br />
<br />
２、在收视实践中，发现卫星波束在天线锅面上的反射，实际上是个别锅瓣（只有靠近偏焦的两瓣）起主要作用的，其他锅瓣反射信号很少，（编者注：偏焦使用时有可能是这样，主焦使用时不可能是这样。否则主焦就不再是主焦，而是没有聚焦，成为偏焦。在偏焦工作时，天线效率要降低）这让我想到如果摘下一瓣或两瓣只要焦点准确收Ｋｕ波段应该没有问题。因为只有个别锅瓣反射信号较多，所以偏焦高频头应正对该锅瓣，也就是偏焦高频头略微向外或者上掰一点点信号最好。 <br />
<br />
３、关于夹具的选择：由于一锅多星属于“不正常接收”所以夹具一般要自己制作。像１００．５°Ｅ和１０５．５°Ｅ两颗卫星相距５°，接收天线又小（１．５米），两个高频头必须挨得很近，所以我把１００．５°Ｅ的馈源盘左边挖一直径和１０５．５°Ｅ高频头直径相当的缺口，由于馈源盘只对弱信号起作用（编者注：是这样吗？），收１０５．５°Ｅ星干脆就不用馈源盘，以此减轻重量并便于固定，然后微调至信号最好的接收角度，用铁丝固定牢稳即可。当然广大烧友自己动脑筋设计更好的夹具最好。 <br />
<br />
４、以前有烧友反映兼收信号的高频头放在左边还是右边好，地理位置对其有什么影响等问题，我根据波束反射原理绘制了一张图，实线代表１００．５°Ｅ星的波束反射路径，虚线代表１０５．５°Ｅ星的波束反射路径。接收点经度１１３°是在河南的某地接收，接收点经度９６°是在四川的某地接收，我们可以看到高频头的左右旋转根本不受地理位置的影响。<br />
<br />
亚洲2号、亚洲3S一锅双星溃源盘，这种溃源盘就是将原高频头随盒装来的盘子在西面用电切割锯开了一个直径7厘米的缺口，在外环上钻两个直径3毫米的孔，在做一个半圆抱箍，将C头固定在缺口内（注意是在西侧啊，位置很重要呦！）这样就不存在调整问题了。基本上是一装就成功。这种溃源盘接收效果大大提高，PBI-1000接收机信号强度没变化还是81，可是信号品质却大大冲到了89―97%，真是轻轻松松一锅双星。亚2 、亚3S信号余量大大。（自己动手能力强工具齐全，完全可以自己加工--否则就找他邮购） <br />
