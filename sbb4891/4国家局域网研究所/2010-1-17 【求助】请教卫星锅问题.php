二、 天线的保养 <br />
卫星天线安装调试完成后，在接收某确定卫星的电视信号时，其方位角、俯仰角基本不动。为消除卫星漂移带来的影响，可以根据实际收测效果，定期或不定期对天线进行微调，以便之始终处于最佳接收状态。 <br />
