第十一章【Ｃ／Ｋｕ复合高频头 】 <br />
下面是两则Ｃ／Ｋｕ复合高频头 的制作方法： <br />
一、自己动手用普通C、Ku高频头改装复合高频头 <br />
对于一个业余卫视爱好者来讲,其乐趣莫过于自己动手改造卫视器材.时下有好多卫星均 工作在C、KU波段上，但要数收亚洲2号最为热门，它在C波段约有27个免费频道，KU波段 有14个免费频道，而且节目都很精彩。但照常规收视上述C、KU波段节目需用两面天线（ 一大一小）及两只高频头，再通过0/22KHz切换器来切换。这样接收虽然方便省事，但一 是占地方、二是投资高。而对于工薪卫视爱好者来讲，应节省开支。笔者产生了一个念 头，是否可用一面天线，同时接收C、KU波段节目？回答是肯定的。但买一只C、KU复合 高频头如PBI-4400C/KU高频头需一千多元，价格实在太高了，某些专业刊物广告介绍的 复合高频头价格也要四五百元之多。于是笔者抱着自己动手改造装的想法，用普通的C波 段高频头和KU高频头组合，成功地接收到了C、KU波段的卫星信号，且价格还不到二百元 。经几个月的试用，效果相当满意，它的性能不亚于成品C、KU复合高频头。现把改装方 法介绍给卫视爱好者们。 <br />
（1）高频头的选用 <br />
C波段高频头可选奥斯卡3.4～4.2GHz或美鹰15度K、17度K型。因这种高频头里面垂直振 子与水平振子的隔离物是一根直径5mm的铝棒，而有些C波段高频头里面的隔离物是与外 壳一起浇铸的铝块，象这样的C波段高频头就不便用来改装。至于KU高频头可选用 PBI-1040、ASK-KU07、ASK168型。 <br />
（2）加工 <br />
一般的C波段高频头它的外壳由两部分组合而成，即馈源引导管和谐振腔。首先在谐振腔 底座上相隔120度且向上1cm处，各画一小圆圈，然后在谐振腔底座上开一个口子（有条 件的话，上车床把它底座车掉；没有条件的话，可用钢锯条把底座锯掉），同时在三个 画圆圈处，各打直径2.3mm的孔，并用直径3mm丝攻攻出牙纹，旋上直径3mm*10mm螺丝钉3 枚，作为固定KU头之用，这样改装后即基本上完工。 <br />
（3）装配与调试 <br />
首先把KU头上的塑料盖去掉不用，然后装到C头底座里面，稍旋紧三枚螺丝钉，使KU头不 掉下来（KU头在C头里面能旋转），把组合好的复合高频头装到天线馈源盘上暂时固定好 ，开机接收亚洲2号星上C波段的卫视信号，待收到信号后再调整C头在馈源盘内的高低度 及极化角度，使接收的卫视信号最强，这时固定好C头；再把电缆接到KU头上，接收KU波 段卫视信号，同时旋转KU头的角度，也使接收到的卫视信号最强，这时可紧固三枚螺丝 钉，使KU头在C头里面不松动为止。调试完毕后，取下复合头，并在KU头与C头连接处打 上玻璃胶或尼龙胶，待固化后就可正式使用了。C波段、KU波段可通过0/22kHz切换器来 切换。 <br />
