<br />
初烧手册<br />
<br />
第一章【学习基础知识】<br />
一、要想接触这些东西，要先了解一定的知识<br />
朋友首先应该浏览些相关网站的介绍（尤其是一些好的论坛，都要放在收藏夹中，每天浏览下有什么新的东西），然后再在QQ中加几个实在的懂行朋友，我的列表中就有些不错的朋友，碰到问题一般都能在忙中帮我解答，有些不实在聊一阵，发现后就删除掉算了！<br />
二、要想看卫星电视，应该具备这些硬件<br />
（1）接收机<br />
现在基本上都是数字的了。种类很多，牌子也不少，但分类我想也就4种<br />
A、&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;免费机器（只能收免费节目，锁码频道收不到）<br />
B、&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;插卡数字机&nbsp; &nbsp; （配合卡，可以收看锁码频道）<br />
C、&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;接收卡&nbsp; &nbsp;&nbsp; &nbsp;（需要电脑支持，用软件解部分锁码频道）<br />
D、&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;模块机&nbsp; &nbsp;&nbsp; &nbsp;（价格稍贵，需要模块和卡）<br />
（2）天线<br />
天线尺寸大小不等，又有正馈、偏馈之分。不同星上的节目由于卫星位置和功率选用不同的天线尺寸和种类。<br />
（3）高频头<br />
牌子也很多，有配合正馈使用的C头，和偏馈使用的KU头。<br />
（4）馈线<br />
就是咱们无论看有线电视或者收无线电视信号用的75-5同轴电缆，当地都可以买到，这个东西质量可很重要，一般天线和接收机的距离不要超过50米，否则信号就会衰减拉，如果馈线太差的话，我想30米就不成了。所以一定要选择质量好的。<br />
<br />
（5）F头<br />
馈线与高频头、馈线与接收机连接部分所用，一般你买机器经销商都免费提供的。想看卫星电视，只要具备上述器材基本上就可以拉。<br />
<br />
<br />
第二章【天线】<br />
一、确定要收看的节目<br />
下面咱们该说你想看什么节目了，只有了解这些，才知道选购什么样的器材！<br />
节目在什么地方？<br />
<br />
打开： <a href="http://www.cnsat.net/" target="_blank">http://www.cnsat.net/</a><br />
这里是我国上空可以收看到的卫星和每个卫星上的节目内容，哈哈，慢慢看吧，很多啊！用一段时间选选看什么节目，推荐看（亚太2R 76.5°E/中新一号 88.0°E/亚洲二号&nbsp;&nbsp;100.5°E/亚洲3S 105.5°E/鑫诺一号&nbsp;&nbsp;110.5°E/马布海一号 146.0°E等）---大部分是中文节目啊，能看懂，如果您非要看外文节目想学外语、看看国外生活方式等，可以啊，这里面也有很多。还不成的话，您就看别的星。<br />
<br />
二、选天线<br />
选好了吗？好的，选好之后，强烈需要您了解的是下面两条<br />
（1）频率在3、4千的那些都是C段节目，需要正馈大天线（直径1.2米--好几米）频率在10000多的那些都是KU段节目，用偏馈小天线（直径0.35米---1.2米，一般偏馈都不是正圆，这个尺寸是短边的，长边也不会长很多）<br />
（2）知道这些后，您就要选择天线大小了，还是这个网站每颗星下面都有各个地区场强覆盖图，根据KU和C的场强选择天线尺寸大小（对照下面的图表选择天线尺寸）<br />
<br />
为什么要先选择天线？因为首先你要考虑你自己地理位置和住所能不能安装大个的天线，否则再好的节目不也没有办法看吗？<br />
<br />
第三章【接收机】<br />
一、根据收视节目确定要哪种接收机<br />
有了上面的基础，下面我们就要看选用什么样的接收机了。<br />
1、如果你只想看免费节目那简单了，普通数字机价格都很便宜机器价格300多元。<br />
2、如果想看锁码的节目，那就要需要买卡机，配合卡可以看一些锁码台<br />
3、想价格便宜，还能看高档节目那就选择电脑接收卡，但必须配合电脑使用，如果有不错的电脑，这个也是个不错的选择。<br />
4、如果您的钱很富裕的话，买模块机不错的啊。 <br />
二、卡机的种类和加密系统<br />
下面我就对卡机的一些东西，写出来。（既然买卡机，我知道大部分人肯定是喜欢用盗版卡来看节目的。正版太贵，我是无法承受，而且也没有必要，不知道您怎么样，那么我就说D版的内容拉）<br />
首先你要了解现在卫星上的锁码频道的加密形式，这个我知道的不多，就把我知道的说说。当然无论您选择什么卡机是都能看免费节目的拉！<br />
（1）VIACCESS法国电信2代系统<br />
百胜2500/3000/3900等VIACCESS法国电信2代系统，现在的主流机型配合AP6 D卡，可以解76.5上TVBS一组、CR一组锁码频道。88上3632一组 等节目。<br />
（2）IRDETO爱迪德系统<br />
火箭机/银河等IRDETO爱迪德系统配合国内卫视卡 ，可以解110.5上面的国内平台，没有几个台（中央几个和一些地方台）<br />
（3）双系统机器（VIACCESS2和IRDETO）<br />
航科410V/I双系统机，支持VIACCESS法国电信2代和支持IRDETO爱迪德系统。相当于上面1、2两款机器加在一起，可以插2卡（410-AP6和国内CB）看上面锁码频道 <br />
航科410单N南瓜系统卡机+卡（146+88） 可解88度，146 的南瓜系统。<br />
现在410可是热门机器，可以自己改系统成V+V/V+I/单N 等系统 呵呵如果要是三个系统V+I+N就好了，当然这不是现实。<br />
<br />
（4）1020双汉卡<br />
1020双汉卡，用软件可以解很多加密节目，不过要配合电脑，我没有研究过，可是价格便宜哦！价格很有诱惑啊。<br />
（5）收看上面所有系统节目的方法<br />
如果真的想看上面的所有系统节目的话，有三种方法<br />
（1）&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;多买几台接收机啊。<br />
（2）&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;买双汉卡<br />
（3）&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;模块机：<br />
现在模块机有4355/9500/DVB2000等，配合万能模块+6IN1卡的话，可以解现在卫星上所有D卡可以解的节目啊。不错，不过价格高啊。<br />
<br />
选好接收机后，要注意选择盗版卡没有任何收视保障的哦，不过Z卡太贵，呵呵熊掌和鱼就是这个样子。<br />
<br />
三、购买设备<br />
了解自己需要看什么节目，选好机器后，看看自己需要几面天线，都是什么样子和尺寸一块买来，不过要找个信誉好的经销商啊。不要成了肉包子，因为这东西本身在国内收看就是违反规定的。到时候被JS骗了也是哑巴吃黄连啊。您可以多在网上转转就会发现大部分销售商还是都不错的。不过你不要计较几十元的差价，选个有质量保证的销售商最重要呦。不过现在在网上确实也存在一些网站报价太高的---不买他的货（糊弄我等不知道行情吗？）。至于哪里经销最好，呵呵，我就不“此地无银三百两”拉，买器材后自己体会吧。<br />
<br />
选好经销商，你还要了解交易方式，现在流行的有几种：<br />
1、邮购（先打款）<br />
2、长途汽车带货（款到付款--不错啊）<br />
3、货运公司（有的可以带收货款-货到付款，有的要先打款的）<br />
<br />
如果您需要安装多面天线的话，不要忘了买0/11K 、0/22K 、DS4切1等开关啊，他们是多星同时接到一台机器里面的必要元件啊，我的观点2面就选择0/22K，3、4面就选择DS开关。至于怎么安装拿到货后看开关和接收机的说明就全懂拉。至于超过4面天线如何用这些开关组合接收的话，那我就不知道了，好象布线很复杂哦，找经销商问吧。或者知道的朋友给画几张图上来。（这里注意买机器的时候一样问问销售商接收机对以上开关的兼容性如何，否则，一下不能用的话，您可就麻烦了，一般更改开关方案多星接收是完全没有问题的）<br />
<br />
价格商定好后，您可能已经开始汇款。网上购物也是一种新的尝试，虽说能够买来本地买不到的东西，但是在等待所购物品的日子里，虽说相信自己的选择是正确的，但那种被骗的感觉时隐时现地出现在脑海中，直到收到领取物品的电话通知，才放下心来。万事开头难，总算是过了这一关。呵呵，所以说嘛，避免这种焦虑的心情，那您就选择个好的经销商。<br />
好了言规正传，在这个等待过程您不要光焦虑，好的器材销售商马上就会把货送到您手中的。这个时候您需要准备的东西还很多，准备好了之后，您的货到了之后，就可以马上安装拉，否则下面这些东西您也不一定1、2天能准备齐全啊。在等待货物的时候您需要做什么呢？请看下章【第三章&nbsp;&nbsp;接收准备】<br />
<br />
第四章 【接收前准备】<br />
在等待器材到货这段时间，我们还有很多东西要准备：<br />
（1）天线安装的位置<br />
调查下您的天线的安装位置，天线前方不要有障碍物，否则就无法看卫星TVRO拉。<br />
（2）天线如何安装<br />
选好安装地点之后，考虑自己的天线如何安装，小天线一般都可以用膨胀螺丝固定在墙上，大天线要看什么固定形式拉，有立柱和盘式等安装形式。<br />
您的地点如果可以随便安装的话，那就用膨胀螺丝直接固定，最好。如果条件不允许的话，那也有方法：您可以用很厚重的铁板上面固定，一般不会吹走的，还不行的话，那么就找来混凝土自己打个方台，无论什么立柱形式都可以采用这种方法啊，而且还可以随便搬走啊，当然您要做的厚重些，这样承受风力要大一些。再有向1.5米的正馈盘式结构的，可以在盘式上面压块长木版，然后用口袋装石子、沙子等压在上面，也是很不错的方法啊。这些都应该在安装之前准备停当才好。<br />
（3）工具的准备<br />
冲击钻、改锥、扳手这些工具要准备好，安装时候都要用的上的，至于固定时候需要的膨胀螺丝，在您买的天线包装中都有，一般不用准备。<br />
（4）准备馈线<br />
根据天线和您接收机的位置准备好合适长度的馈线。<br />
（5）下载寻星软件<br />
最重要的是这个拉：<br />
&nbsp; &nbsp; 下载寻星软件abxx.exe<br />
这里面有我国各个地方的经度、纬度和接收卫星的时候天线所调的仰角和方位角、极化角<br />
<br />
（6）记录所需参数<br />
准备一张纸上面写下你需要调的卫星所需要的三个角度和这颗卫星上节目的信号最强参数（用于寻星），最弱参数（用于细调达到最佳效果和固定）<br />
比如：76.5(KU)：最强：12730V30000&nbsp; &nbsp; 弱：12642H21500或12278 V 22425 <br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;88（C）：用3582 H 12860 凤凰一组调&nbsp;&nbsp;3632 V 26667 一组固定<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;100.5（C）：最强：3720 H 4420 福建卫视&nbsp;&nbsp;4000 H 28125 DW <br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;最弱：3856 V 6812 山东卫视 等<br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;105.5（C）：最强：4094 H 5555 阳光卫视&nbsp;&nbsp;最弱：4109 H 11230 TVB <br />
&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;146（KU）：我没有接收，强度不知道，希望接收的朋友贴下<br />
（7）准备测量用器具<br />
（1）&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;准备指南针（实际上指南针我认为没有什么大的必要，方位角大体可以估计出来的）<br />
（2）&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;量角器、铅垂（重物+细线DIY），用于量天线的仰角和方位角<br />
（3）&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;水平仪（可能用的上，如果太不平了的地方最好加工下，否则影响您的调星）<br />
（4）&nbsp; &nbsp;&nbsp; &nbsp;&nbsp;&nbsp;呵呵这些东西实际上我没有用，我和朋友找了个罗盘（比较高级），角度（仰角、方位角）、水平、指南等这个东西都是可以做到的。<br />
<br />
第五章【正装还是倒装天线】<br />
对于正馈大天线来讲就没有这些内容了，您只需用上面软件计算出来的参数直接调就可以了，但是偏馈小锅下面的内容您必须要看了。<br />
一、正装偏馈<br />
例如：中卫S60的长轴=63CM，角度差的测定稍麻烦一点，办法如下：用该天线准确对准某颗星，注意仰角必须精调至最佳，用铅垂线和直尺测量出这时的后倾量，利用以上公式可以解出“角度差”，例如，中卫S60角度差=24.3度。这样以后你不论在何地也不论要调那颗星，只要事先获得“卫星仰角”前提下，都可以很快用该公式计算出决定仰角的后倾量，卫星仰角有许多途径可以获得，这里不再赘述。<br />
<br />
也就是说正装天线调仰角的时候要再软件计算出来的角度上减去20度（75CM天线），比如我的地区收76.5的时候软件计算的仰角是28度，在安装调试的时候应该调成8度。通过这个图形，您基本上对正馈和偏馈的区别也了解了吧<br />
二、倒装偏馈<br />
为什么要有倒装呢？您没有发现吗？倒装的时候天线仰角很大吗，实际上是在软件计算出来的角度上面要加上20度，比如计算是28度的仰角，这样在调星的时候就需要48拉。 <br />
<br />
这么做倒装和正装有区别吗？当然有了，通过刚才8度和48度的比较，应该发现用处了吧，比如您安装地点前面有障碍物的话，倒装就是很好的选择啊。通过比较接收效果没有差别的。<br />
<br />
至于侧装，呵呵我等初烧搞那么复杂做什么啊！实际上，我也不知道呢。<br />
<br />
第六章【如何调极化角】<br />
在刚才咱们软件所计算出来的仰角和方位角，我想您一定都明白了，还有一个很重要的就是极化角，这个调不准确的话，节目质量可会受到很大影响啊，KU极化还好一点，可以C极化调不好的话，那可就麻烦了，您可能根本就调不出节目来啊，我可有体会。<br />
<br />
先说KU高频头上都有一个标志，KU高频头标志向上就是零度位置，或者是F头指向右下45度的时候是0度，逆时针旋转为负，顺时针旋转为正（参考下面图形）<br />
那么C头呢？C头的极化角看头上的0刻度，大多高频头0刻度水平的时候为0度（也有不一样的，有垂直是0度的），比如我们就让0刻度指向东吧为0，然后顺时针为正、逆时针为负，呵呵无论向哪个方向关键是灵活运用。<br />
注意：偏馈无论正装还是倒装极化角调整是一样的<br />
