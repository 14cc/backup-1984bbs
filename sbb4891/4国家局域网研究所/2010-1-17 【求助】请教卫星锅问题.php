第十五章【卫视知识】<br />
经过一段时间的学习，相信您精彩的节目已经欣赏起来拉，知识也已经了解了不少，如果您有兴趣的话，就系统的学习、简单了解下这章的内容。 <br />
一、数字卫星电视系统简介 <br />
数字卫星电视是近几年迅速发展起来的，利用地球同步卫星将数字编码压缩的电视信号传输到用户端的一种广播电视形式。主要有两种方式。一种是将数字电视信号传送到有线电视前端，再由有线电视台转换成模拟电视传送到用户家中。这种形式已经在世界各国普及应用多年。另一种方式是将数字电视信号直接传送到用户家中即：Direct to Home（DTH）方式。美国Direct TV公司是第一个应用这一技术的卫星电视营运公司。与第一种方式相比，DTH方式卫星发射功率大，可用较小的天线接收，普通家庭即可使用。同时，可以直接提供对用户授权和加密管理，开展数字电视，按次付费电视（PPV），高清晰度电视等类型的先进电视服务，不受中间环节限制。此外DTH方式还可以开展许多电视服务之外的其他数字信息服务，如INTERNET高速下载，互动电视等。 <br />
DTH在国际上存在两大标准，欧洲的标准DVB-S和美国标准DigiCipher。但DVB标准逐渐在全球广泛应用，后起的美国DTH公司Dish Network也采用了DVB标准。 <br />
（1）DTH系统的组成<br />
一个典型的DTH系统由六个部分组成： <br />
1）前端系统（Headend) <br />
前端系统主要由视频音频压缩编码器，复用器等组成。前端系统主要任务是将电视信号进行数字编码压缩，利用统计复用技术，在有限的卫星转发器频带上传送更多的节目。DTH按MPEG-2标准对视频音频信号进行压缩，用动态统计复用技术，可在一个27MHz的转发器上传送多达10套的电视节目。 <br />
2）传输和上行系统（Uplink) <br />
传输和上行系统包括从前端到上行站的通信设备及上行设备。传输方式主要有中频传输和数字基带传输两种。 <br />
3）卫星（Satellite) <br />
DTH系统中采用大功率的直播卫星或通讯卫星。由于技术和造价等原因，有些DTH系统采用大功率通讯卫星，美国和加拿大的DTH公司采用了更为适宜的专用大功率直播卫星（DBS）。 <br />
4）用户管理系统（SMS） <br />
用户管理系统是DTH系统的心脏，主要完成下列功能： <br />
A. 登记和管理用户资料。 <br />
B. 购买和包装节目。 <br />
C. 制定节目记费标准及用户进行收费。 <br />
D. 市场预测和营销。 <br />
用户管理系统主要由用户信息和节目信息的数据库管理系统以及解答用户问题，提供多种客户服务的Call Center构成。 <br />
5）条件接收系统（CA） <br />
条件接收系统有两项主要功能： <br />
A. 对节目数据加密。 <br />
B. 对节目和用户进行授权。 <br />
目前国际上DTH系统所采用的条件接收系统主要有：美国NDS，以色列Irdeto，法国Via Access，瑞士Nagra Vision等。 <br />
美国Direct TV公司以及采用Direct TV技术的加拿大Star Choice公司使用的是NDS条件接收系统；美国Dish Network（Echostar)公司以及采用Echostar技术的加拿大Bell ExpressVu公司使用的是Nagra Vission条件接收系统。 <br />
6）用户接收系统（IRD） <br />
DTH用户接收系统由一个小型的碟形卫星接收天线（Dish)和综合接收解码器（IRD）及智能卡（Smart Card)组成。 <br />
IRD负责四项主要功能： <br />
A. 解码节目数据流，并输出到电视机中。 <br />
B. 利用智能卡中的密钥（Key)进行解密。 <br />
C. 接收并处理各种用户命令。 <br />
D. 下载并运行各种应用软件。 <br />
DTH系统中的IRD已不是一个单纯的硬件设备，它还包括了操作系统和大量的应用软件。目前较成功的IRD操作系统是Open TV。美国Dish Network公司已开始逐步升级用户的IRD为Open TV系统。 <br />
二、什么是地球同步卫星 <br />
地球同步卫星就是在离地面高度为35786公里的赤道上空的圆形轨道上绕地球运行的人造卫星。其角速度和地球自转的角速度相同，绕行方向一致，与地球是相对静止的。 <br />
三、IRD是什么 <br />
IRD（Intergrated Receiver Decoder)是指综合解码卫星接收机。 <br />
现在基本上都是数字接收机了，模拟信号已经基本淘汰，只有很少量的星上有模拟节目。当然我们要选择的IRD也都是数字的 <br />
<br />
四、什么是TVRO <br />
TVRO(Televsion Receive Only)就是针对各卫星所传送的讯号,尝试加以接收和解调但并不发送。 <br />
<br />
五、什么是DiSEqC <br />
DiSEqC标准介绍――DiSEqC英文为Digital Satellite Equipment Control，直译为“数字卫星设备控制”，有1.0、1.1、1.2、2.0等不同版本的标准，是用数字卫星电视接收机控制，发出指令集（控制指令）给相应设备，如切换开关、切换器、天线驱动设备、LNB等。 <br />
工作过程是数字卫星电视接收机内部在同步时钟脉冲配合下，通过与LNB高频头相连的同轴电缆线，经调制于22KHz频率上交替变化的数字信号串行转送相关控制指令。 <br />
DiSEqC1.0常用于控制多入一出的中频切换器的控制； <br />
DiSEqC1.1是1.0的扩充版本； <br />
DiSEqC1.2则加入驱动并控制推动杆或极轴座的功能； <br />
DiSEqC2.0就具有双向控制的功能，外设就会有信息传回数字卫星电视接收机。 <br />
转帖]DisEcq切换开关的原理及使用 （作者：李星漫） <br />
0/12v,0/22k,13v/17v,DisEcq切换开关的原理及使用 <br />
经常有些初级烧友对各种切换开关的使用和设置无所适从，网上也没见高手的系统介绍，现抛砖引玉，不当之处敬请指正。 <br />
0/12v,0/22k,13v/17v这三种开关都是继电器式二路切换，原理相当于一个单刀双掷开关，不同的是触发的方式，0/12v是靠接收机单独给开关提供的12v电压的有无，来控制继电器的开启选择信号（好绕口哟^_^）。因其要单独给开关引一根电源线，开关不宜据机器太远，故现在大多数接收机已无此功能。0/22k开关是通过接收机发出的22k脉冲信号来触发继电器，13/17v开关是通过17v电压来触发继电器。和DisEcq开关一样，他们都是通过同轴缆传递控制信号，无需给开关单独走线。其实22k的主要用途是用以控制双本振高频头本振的选择，如果系统中有双本振头，就不要选用22k开关来控制天线了（其实是LNB的切换），同理，如果系统中用的是双极化单输出的高频头，也不能用13/17v开关。鉴于以上三种开关使用中的限制，以及继电器的机械寿命和高频衰减，于是出现了一种能同时控制四路天线、无需另外电源、也不与高频头抢控制信号、没有继电器寿命和衰减等种种缺陷的切换开关，那就是DisEcq，他由飞利浦公司研制，开关的核心是一块cpu而不是继电器，由接收机输出的信号控制四面天线切换，刚上市时仅一块cpu售价就几十美元，成品开关就更贵了，现在价格已基本合理，一般一个D开关价格在50元人民币左右，比前三种开关贵一倍，但性能却好好多。 <br />
以上的开关是可以配合串用的，如果你的接收机全部具备上述功能，算一算最多能控制几面天线？ <br />
如果你系统中没有双本振头，可以控制16面天线，而且不影响双极化头工作，如果不仅没有双本振头，还都是单极化的，那么最多可控制32面天线！厉害吧？ <br />
<br />
<br />
六、什么是lnb <br />
lnb俗称降品器（也就是高频头），顾名思义就是把频率降低，降到多低算合适呢，这就要看一下接收机的lnb端口的输入要求了，就一般接收机而言，它的输入要求在950-2150（mhz）之间，所以我们只要简单的算一下就可以了，但是现在我要提醒一下大家，c波段和ku波段的算法是不一样的，也就是说ku波段的算法是[频率减本振]，而c波段的算法正好与ku波段相反是[本振减频率]，得出的数只要在我们的接收机要求的范围内就可以了。当然还有最好的方法，那就是我们选择双本振的lnb就可以全收了！ <br />
lnb（low noise block downconverter）就是低讯降频放大器一般可分为c频lnb（3.7ghz-4.2ghz）和ku频lnb（10.7ghz-12.75ghz）。因卫星讯号在抵达天线前已相当微弱及同轴电缆传输的频率越高讯号损耗越大，所以才需要lnb来做改善。lnb的工作流程就是先将卫星高频讯号放大至数十万倍再利用本地振荡电路将高频讯号转换至中频950mhz-2050mhz(依lnb种类决定中频范围)，以利于同轴电缆的传输及卫星接收机的解调和工作。<br />
