																											<div id="postmessage_285195" class="t_msgfont">2009-10-14 19:49:25<br />
<br />
在许多国家，用电骡等P2P下载软件属于违法乱纪的行为。以后要是到了美国，Youtube是可以上了，但电骡不能用了。对于这一点，我还是会很伤感的。<br />
<br />
很多国家对于电骡的围剿，主要是出版权的原因。版权是资本主义的产物，里面有资本主义的精神本质，所以人家是很在乎这个的。但社会主义社会是个窃书不算偷的大公社会，我国的社会主义虽然具有中国特色，但在版权的问题上依旧保留了社会主义的优越性。所以在我国，电骡的生存环境就优渥多了。其实何止是电骡，土豆优酷什么的，ZF也都是很支持的么。要和谐就不能照抄民主，要发展就不能硬来版权，在这这一点认识上，ZF是英明神武的。<br />
<br />
但我觉得电骡迟早得出事情。它之所以还没出事情，是因为它的某个功能非常低调，如果再华丽一点，早就被有关部门嘣掉了。这个功能就是隐藏在电骡软件里的Kad网络搜索。我在本科的时候还研究过一阵Kad网络的结构原理，所以深知它的危害性。这个危害性今天再次加深了我的印象，因为我今天又下载了不该下载的东西……用的不是别的，就是电骡。<br />
<br />
出于一种低俗的暴露欲望，我便忍不住想谈谈Kad网络。Kad，简言之，是个典型的“去中心”的搜索系统，再打个比喻，就是个组织良好Gossip（小道消息）网络。例如，你可能时不时地遇到这样的情景：你需要找张三，最有效的办法，应该是去公安局调档案，但你不想这么做。因为一来张三可能是个敏感人物，或者你企图可疑，公安局不会告诉你他的信息，而且他有权这么做；二来公安局也可能在找张三，你去找公安局，公安局也就盯上你了，等你找到了张三，公安局也就找到张三了，这是很伤感情的事情。基于诸多顾虑，你得通过其他途径找张三。<br />
<br />
选择之一就是，你就可以去Kad网络上结交一些邻居，这些邻居虽然知道的不多，但是他们有个习惯，就是定期会交流信息，然后把与自己名字雷同的人的材料记录下来。所以王麻子很可能记录了王二麻子的信息，张二也可能知道张三的动静。信息就这样被均匀地分布式地储存在了邻居们的心中。很凑巧，你的邻居里确实有个人叫张二，于是你去拜访他，问他认不认识张三。张二查了一下他的记事簿，说他不认识张三，但认识一个叫张小三的人，他比自己的名字更接近张三，所以他也很有可能知道张三的情况。于是你就得到了张小三的地址。如果张小三也不知道张三，他同样也会提供更接近张三名字的人的信息。以此类推，你就逐个访问。没过多久，在很大的概率上，你就能收敛到张三的家门口。你的目标也就完成了。如果你还有心情，你就可以回访张小三和张二等人，向他们表示感谢，然后把你找到张三的事情告诉他们。他们就会更新他们的记事簿，把张三的信息记录下来。<br />
<br />
这就是Kad网络信息存储、更新和搜索的基本原理。网络中，每个用户（节点）都被分配了一部分存储和搜索的任务，他们互帮互助，互守相望。这也就是传说中的分布式搜索引擎。在这个过程中，值得注意的是，没有一个节点处于中心的位置，也就是说任何一个节点都可以临时失效（比如被警察叔叔关心一下），而这并不根本性地影响你的搜索。当初谷歌被关心的时候，大家都很不方便，因为要搜索什么都必须通过谷歌的服务器，谷歌down了，也就什么都没有了。然而当你处在一个Kad网络中，为了到达某个地方，你可以有很多种路径的选择。这种独立的稳健性，就是Kad可怕的地方。<br />
<br />
传统的BT发布，是通过论坛这个媒介。这就跟俱乐部差不多，信息的交流效率在俱乐部范围内还是很高的，有个什么事情，整个俱乐部的人都能很快知道。但缺点是，俱乐部得有个固定场所，还得向有关部门申请经营许可证，所以很容易被和谐。就算不被和谐，俱乐部的墙上也很可能挂着八荣八耻之类的东西，随时准备对你要发布的东西做一番审查。这是很不令人愉快的。（比如Maze，你连搜索“苍井空”都不行，但是它的优点你是用不着搜索，随便找个邻居在他文件夹里翻翻，就能找到你要的东西，物以类聚嘛……）但在Kad网络中，这种审查是有相当的难度的。因为它没有固定的场所，因为它散布在大街小巷，因为它是一散播出去就收不回来的Gossip。也正是如此，在电骡的Kad搜索当中，你可以搜到很多在百度和迅雷上搜不到的资源。比如前段时间的《XX蹄花》，现在百度上搜索出来的页面基本是被和谐过的，但是电驴上的Kad搜索依旧可以搜到相关内容，同时可以稳健地下载。<br />
<br />
